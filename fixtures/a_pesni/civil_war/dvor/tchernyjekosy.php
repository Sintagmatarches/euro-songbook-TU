<br>
Черные косы имела, <br>
      В них черные ленты плела, <br>
      Милого крепко любила, <br>
      За что и в могилу ушла.<br>
<br>
Он был из семьи коммунаров, <br>
      Она же — кулацкая дочь.<br>
      И как же сошлась эта пара, <br>
      То знает весенняя ночь!<br>
<br>
То знает былинка, качаяся в поле, <br>
      То знает тростник молодой, <br>
      Как черные косы и черные ленты <br>
      Ласкались несмелой рукой!<br>
<br>
И ты его крепко любила, <br>
      И так же любил он тебя. <br>
      Но выжечь коммуну той темною ночью <br>
      Враги сговорили тебя.<br>
<br>
Наутро, расследуя место пожара, <br>
      Он черную ленту нашел <br>
      И понял, бедняга, что враг был коммуны, <br>
      Кого он любил всей душой,<br>
<br>
Ее он не выдал, но, встретившись с нею.<br>
      Сурово и мрачно сказал: <br>
      — Возьми эту ленту. Вчера потеряла, <br>
      Когда поджигала сарай!<br>
<br>
И жизнь показалася темною ночью! <br>
      Ты в ужасе косы рвала. <br>
      И черные воды колхозного пруда <br>
      Вчера схоронили тебя! <br>
	  <br>
      <em>В нашу гавань заходили корабли. Пермь, &quot;Книга&quot;, 
      1996.</em><br>
<br>
<br> 
