        Многие красноармейские песни закрепились в устной традиции в поздней, 
        &quot;сталинской&quot; редакции - конца 1920-х - начала 30-х гг.; обращаясь 
        к текстам, следует это иметь в виду. Вычеркивали имена неугодных полководцев 
        и т. д. либо добивались &quot;литературной красоты&quot; (одновременно 
        получая авторские права; именно в таком виде их исполнял знаменитый Ансамбль 
        военной песни Александрова).<br>
        <br>
        Песни, собранные фольклористами после войны (активный сбор начался с 1931 
        г.) также могли претерпеть изменения за годы своего бытования в устной 
        форме. Не исключено, что некоторые из них были написаны не в ходе самой 
        войны, а по ее горячим следам. Если есть какие-то доказательства, что 
        песня реально бытовала в годы войны (мемуары, публикации), то здесь они 
        приводятся.<br>
