Таежные пади,<br>
Бескрайние дали,<br>
Туман над рекою повис.<br>
Лежит под сосной<br>
Над Леной-рекою<br>
Алымов, боец-коммунист.<br>
В оковах железных<br>
И в годы подполья<br>
Этапом прошел на Якутск.<br>
Мохнатые кедры,<br>
Суровые дебри<br>
Навек полюбились ему.<br>
Под залпы «Авроры»<br>
Он вышел на битву<br>
С врагами родимой земли.<br>
За волю - народам,<br>
За землю, заводы,<br>
За счастье детей трудовых.<br>
Пробились в атаках<br>
Под Керенск отряды,<br>
А бой все гремит и гремит.<br>
Иссякли снаряды,<br>
Не спят партизаны,<br>
Колчак им расправой грозит.<br>
Ночною порою<br>
Враги навалились,<br>
Ушли партизаны в тайгу.<br>
Отход прикрывая,<br>
Бесстрашный Алымов<br>
Был схвачен врагами в бою.<br>
Ночные допросы,<br>
Жестокие пытки<br>
Бойца не свернули с пути.<br>
В селе на рассвете<br>
С друзьями расстрелян<br>
Алымов - герой-коммунист.<br>
Живет, побеждает<br>
Бессмертное дело;<br>
Страна к коммунизму идет.<br>
В труде и учебе<br>
Алымов - как знамя,<br>
На подвиги смелых зовет.<br>
