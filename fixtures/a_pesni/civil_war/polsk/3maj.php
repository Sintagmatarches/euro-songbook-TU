  <br>
  Та najsłynniejsza z pieśni opiewających Konstytucję Trzeciego Maja (1791), powstała 
  w czterdziestą rocznicę uchwalenia Konstytucji, podczas powstania listopadowego, 
  w kwietniu 1831 roku. Jej tekst napisał poeta i autor wierszy patriotycznych 
  Rajnold Suchodolski, który początkowo uczestniczył w powstaniu jako żołnierz 
  gwardii honorowej, а następnie jako podchorąży V Pułku Strzelców Pieszych. Tekst 
  ten napisał do popularnej już wówczas melodii (prawdopodobnie autorstwa Fryderyka 
  Chopina), do której w okresie przedpowstaniowym śpiewano piosenkę konspiracyjną 
  <em>Nienawidzę was, próżniaki</em> ze słowami Stanisława Doliwy-Starzewskiego, 
  ps. Stach z Zamiechowa. (Melodia Chopina pozostała juz przy tekście Suchodolskiego, 
  natomiast tekst Starzewskiego z refrenem "Otóż maj, piękny maj, zieleni się 
  błoń i gaj&quot; - zaczęto śpiewać w drugiej połowie XIX wieku z melodią skomponowaną 
  przez Stefana Surzyńskiego.) <br>
  <br>
  Po odzyskaniu niepodległości, kiedy dzień 3 maja ogłoszono świętem narodowym, 
  pieśń ta wiązała się z ceremoniałem podnoszenia flagi państwowej na Placu Zamkowym 
  w Warszawie w dniu tego święta. W 1919 roku wprowadzono do obiegu śpiewanego 
  zupełnie zmieniony tekst Suchodolskiego. Zmian tych dokonał poznanski poeta 
  Adam Kompf (prawdopodobnie na zamówienie władz, które chciały związać tę pieśń 
  ze świętem państwowym). Otóż w ostatnim wersie pierwszej zwrotki oryginału, 
  usunął on słowa "przy hulance i przy winie&quot;, zastępując słowami: "która 
  w całej Polsce słynie&quot;. Następnie wyrzucone zostały pozostałe zwrotki Suchodolskiego 
  (uzasadnienie: "niesprawiedliwie traktowały króla Stanisława Augusta Poniatowskiego&quot;). 
  Na temat "niesprawiedliwego&quot; traktowania carycy Katarzyny i cara Mikołaja 
  w następnych zwrotkach Suchodolskiego - już nikt się nie wypowiadał, ро prostu 
  je usunięto. Tak więc do pierwszej, zmienionej w ostatnim wierszu, zwrotki Rajnolda 
  Suchodolskiego, doszły dwie następne oraz refren napisane przez Kompfa. I taka 
  wersja uchodziła za "państwowo prawidłową&quot;. Na szczęście nie zmieniono 
  w sposób "państwowotwórczy&quot; oryginalnej melodii mazurkowej. <br>
  <br>
  Inne tytuły tej pieśni: <em>Mazurek 3 Maja, Trzeci Маj Litwina. </em><br>
  <br>
  Melodie <em>Witaj, majowa jutrzenko</em> włączył Ryszard Wagner do swojej uwertury 
  <em>Polonia</em> (1832). "Polska walka о wolność przeciwko przemocy - pisał 
  Wagner - napełniła mnie rosnącym entuzjazmem...&quot; Można stąd wnosić, że 
  pieśni powstania listopadowego, wraz z wieścią о tym zrywie narodowowyzwoleńczym, 
  wyszły poza granice ziem polskich docierając do świadomości mieszkańców wielu 
  krajów Europy. <br>
  <br>
  ***<br>
  <br>
  Rajnold SUCHODOLSKI (1804-1831), poeta, uczęstnik powstania listopadowego, w 
  którym zginął, autor wielu pieśni patriotycznych. Napisał teksty do pieśni m.in. 
  <em>Witaj, majowa jutrzenko</em> (muz. prawdopodobnie F. Chopin), <em><a href="polonezkos.htm">Polonez 
  Koścsuszki</a></em> (muz. Т. Kościuszko), <em><a href="dalejbracia.htm">Dalej 
  bracia, do bułata</a></em> (mel. ludowa).<br>
  <br>
  <em>Wac&#322;aw Panek. Hymny polskie. Warszawa, Wydawnictwa Szkolne 
  i Pedagogiczne, 1997.</em><br>
<br>
  <br>
  Написана в апреле 1831 г. во время Ноябрьского восстания (Powstanie 
  Listopadowe) и приурочена к 40-летию принятия Конституции 
  Третьего Мая. Автор текста - поэт Райнольд Суходольский - в дни восстания был 
  солдатом почетной гвардии, затем подхорунжим V Полка пеших стрелков. Написано 
  на популярную в те годы мелодию, предположительно принадлежащую Шопену, на которую 
  накануне восстания пелась конспиративная песенка Станислава Старжевского <a href="nienawidze.htm">&quot;Nienawidzę 
  was, próżniaki&quot;</a> (&quot;Ненавижу вас, бездельники&quot;). В дальнейшем 
  песенка Старжевского стала исполняться на другую мелодию. См. также другие родственные 
  песни - <a href="3majnowy.php">&quot;Pamiętasz, bracie kochany...&quot;</a> 
  на слова Константина Гашиньского и <a href="biednykraj.php">&quot;Biedny kraj&quot;</a>.<br>
  <br>
  Конституция Третьего Мая была принята 3 мая 1791 г. при короле Станиславе Августе. 
  Через год, 18 мая 1792 г. последовала агрессия России, и 24 июля Станислав Август 
  капитулировал. Причины этого непонятны, так как накануне, 18 июня в битве под 
  Зеленцами польские войска выиграли, и в честь этого события был введен орден Virtuti 
  Militari. За капитуляцией последовал второй раздел Польши 23 января 1793 г. между 
  Пруссией и Россией, урезавший Польшу более чем вдвое: 58 тыс. км<sup>2</sup> 
  и свыше 1 млн жителей отошли к Пруссии, 280 тыс. км<sup>2</sup> и 3 млн жителей 
  - к России. Оставшееся государство Польское попало под протекторат России - 227 тыс. км<sup>2</sup> и 4,4 млн жителей (вместе с Курляндией). В последующем 
  восстании Тадеуша Костюшко (24 марта - 16 ноября 1794 г.) Конституция Третьего 
  Мая была программой умеренного крыла; радикальное крыло предполагало установление 
  республики.<br>
