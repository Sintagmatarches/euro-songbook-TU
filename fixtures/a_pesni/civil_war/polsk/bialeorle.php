	<span class="header"><strong>Blałe orlę<br>
(Белые орлы)</strong><br></span>
<br>
Słowa: Wincenty Pol<br>
<br>
Mnóstwo ludzi przed gospodą, <br>
Tańczy młodzież rżnie muzyka,<br>
Bywaj zdrowa mi jagodo, <br>
Jutro siędziem na konika.<br>
<br>
А jagoda, а kochanie, <br>
Smutno w tańcu nań spoziera, <br>
То chce śpiewać, to znów stanie <br>
I fartuszkiem łzy ociera.<br>
<br>
Zatrzymała wszystkie pary:<br>
I skinęła ku Warszawie; <br>
Во z Warszawy wracał prawie<br>
Z nowinami Bartosz stary.<br>
<br>
А gdy spostrzegł ponad drogą<br>
Takie tany, potrząsł głową<br>
I pogroził skrzypkom srogo, <br>
A do chłopców rzekł surowo:<br>
<br>
"Dajcie pokój pustej wrzawie,<br>
Bo dziś dzieci wielkie święto, <br>
Dziś okopy przy Warszawie<br>
Z nabożeństwem sypać jęto.<br>
<br>
"А i owo Bóg wysoki <br>
Wielkie cuda nam zwiastuje; <br>
Patrzcie! patrzcie na obłoki, <br>
Co tam z chmur się ukazuje?<br>
<br>
"Wczak to, dzieci, orlę nasze!<br>
Polskich królów orlę one, <br>
Oto patrzcie, jakże ptaszę <br>
Srogo piersi ma skrwawione!"<br>
<br>
"Prawda, prawda!" lud zawoła, <br>
Niechaj będzie pochwalony!<br>
Codźwa ojcze do kościoła, <br>
А wy, chłopcy, bijcie w dzwony.<br>
<br>
I już niebo poszarzało,<br>
Znikł z przed oczu orzeł białyу, <br>
А lud jeszcze klęczał cały <br>
I psów wycie się ozwało...<br>
<br>
I nazajutrz о tym cudzie <br>
O mil kilka powiadano: <br>
"Za Kościuszki — rzekli ludzie — <br>
Orlę takie już widziano".<br>
<br>
"Wielka, wielka wolna będzie! <br>
Oj, niejeden Moskal zginie; <br>
Lecz i orlę krwią opłynie, <br>
W Polsce mogił nam przybędzie!" <br>
