	<span class="header"><strong>МАРИОНЕТКИ &quot;МАХНОВЦЫ&quot;<br>
</strong> <br></span> 
  <br>
  Марионетки попали на &quot;Клизму&quot; в июне 2004 года недоделанными и к августу 
  были окончены. Головы из папье-маше, раскрашены масляной краской, туловища сшиты 
  из мешковины и набиты тряпками. Всего было 12 персонажей: Батька Махно, Федя 
  Щусь, Марченко, Каретников, Галина Кузьменко, Маруся Никифорова, Лева Задов, 
  Пулеметчик, Белаш, а также Китаец, Мексиканка и Панк с дубиной (три последние 
  к махновскому движению не имеют отношения и были сделаны &quot;за компанию&quot;). 
  В ночь с 3 на 4 июля в ходе ментовского налета были спрятаны, повторно спрятаны 
  при оставлении сквота осенью; в дни фестиваля Черный Петроград отрыты и 9 ноября 
  2004 года, накануне гибели сквота, эвакуированы. Все они сохранились.<br>
  <br>
  Из ниже представленных фото 5 сделаны летом 2005 года: Галина Кузьменко, Белаш, Китаец, 
  Мексиканка и Панк с дубиной. Некоторые немного изменились. На &quot;Клизме&quot; 
  Галина Кузьменко была одета в черное, а на снимке - в восточную полосатую одежду; 
  на Белаше было что-то белое - теперь носовой платок. Остальные 7 снимков сделаны 
  весной 2008 года.<br>
