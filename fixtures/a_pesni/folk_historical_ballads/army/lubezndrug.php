	<h1>ЛЮБЕЗНЫЙ 
ДРУГ, УВЕДОМЛЯЮ</h1>
<br>
Любезный друг, уведомляю,<br>
Что кончен наш кровавый бой.<br>
Своих с победой поздравляю,<br>
Себя – с оторванной рукой.<br>
<br>
Я в лазарете умираю,<br>
И труп мой фельдшер уж купил.<br>
Тебе червонец посылаю,<br>
Что он за труп мне заплатил.<br>
<br>
Признаться, скучно быть зарытым<br>
Далёко от родной земли!<br>
Умри я дома – над убитым<br>
Друзья поплакать бы могли.<br>
<br>
Крест деревянный на погосте <br>
Тогда стоял бы надо мной. <br>
А может быть, и ты бы в гости <br>
Пришел ко мне, друг милый мой! <br>
<br>
Собаку - верного Фингала – <br>
Тебе на память я дарю. <br>
Ласкай ее, и чтоб не знала <br>
Она никак про смерть мою. <br>
<br>
Когда я с матушкой прощался, <br>
Старушка так была больна. <br>
Когда узнает - сын скончался, <br>
Умрет, наверное, она.<br>
<br>
Прощай, не плачь! Я умираю, <br>
Тебя мне больше не видать! <br>
В полку, куда я поступаю, <br>
Нельзя уж отпуска достать. <br>
<br>
Вот за священником послали, <br>
Меня готовят в дальний путь, <br>
И вот маршрут мне приписали... <br>
Прощай, и друга не забудь!<br>
