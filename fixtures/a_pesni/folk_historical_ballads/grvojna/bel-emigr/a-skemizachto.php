	<span class="text"><strong>С.М. Маркедонов<br></strong>
  <br>
  <span class="header"><strong>«С КЕМ И ЗА ЧТО МЫ ВОЕВАЛИ?..»<br>
        РУССКАЯ РЕВОЛЮЦИЯ И ГРАЖДАНСКАЯ ВОЙНА В ОЦЕНКАХ ЛИДЕРОВ «ВОЛЬНОКАЗАЧЬЕГО 
        ДВИЖЕНИЯ»</strong><br>
        </span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 24-32</em>.<br>
  <br>
  <br>
  «Русская революция поставила перед всею Россией в целом и перед каждою ее частью 
  проблему самопознания», — констатировал постоянный автор журнала казаков Общеказачьей 
  студенческой станицы в Праге «Казачий сполох» К. Чхеидзе<sup>1</sup>. Казаки, 
  пережившие в 1917-1922 гг. кровавую братоубийственную Гражданскую войну и оказавшиеся 
  после ее завершения в эмиграции, вдали от родных станиц и куреней, были вынуждены 
  не только приспосабливаться к новым (малознакомым и для многих чуждым) социально-экономическим, 
  политическим и социокультурным реалиям, но и заново определять для себя, что 
  есть казачество, чем оно было в прошлом и чем должно стать в будущем? «...Разбросаны 
  мы по всем странам и материкам. Служили мы в иностранных легионах в знойной 
  Африке, ищем счастья в Америке, северной и южной, забрались мы даже в Австралию... 
  Нет, кажется, ни одной страны, где бы сейчас не пробивал себе казак торной жизненной 
  дороги тяжелым и упорным трудом... Но не только искание и добывание хлеба насущного 
  стояло перед казачеством эти долгие годы эмиграции. Как только прошли первые 
  трудные месяцы, и физическая жизнь на чужбине была обеспечена, - перед каждым 
  из нас встали одни и те же мучительные вопросы: с кем и за что мы воевали, кто 
  были наши союзники и кто враги, и почему борьбу мы проиграли?» - обращались 
  с вопросом к казачьей эмигрантской аудитории издатели журнала «Вольное казачество 
  - Вильне козацтво», первый номер которого увидел свет 10 декабря 1927 г.<sup>2</sup><br>
  <br>
  Ответ на вопрос издателей «Вольного казачества» не был и не мог быть однозначным. 
  Свои варианты ответов на него давали представители не только различных казачьих 
  войск, но и практически всех политических лагерей Зарубежной России (от монархистов 
  до различных групп социалистов). Разные версии объяснения причин, итогов и уроков 
  гражданского противостояния в России и роли в нем казачества носили не только 
  памфлетно-публицистический или мемуарный характер, и зачастую определялись вовсе 
  не политической конъюнктурой. Представителями различных идейно-политических 
  направлений казачьей эмиграции были разработаны собственные модели и концепции 
  как истории казачества в целом, так и революции и Гражданской войны в казачьих 
  областях. Между тем историографическое изучение этих исторических концепций 
  до сих пор не стало предметом серьезного комплексного исследования. С одной 
  стороны история казаков в изгнании не является terra incognita. В 1990-е гг. 
  появился целый ряд содержательных работ по истории казачьей эмиграции, которые 
  вывели это социально-политическое и социокультурное явление из «дымки загадочности». 
  В них были рассмотрены такие проблемы, как организации различных движений казаков-эмигрантов, 
  их взаимоотношения с другими эмигрантскими объединениями, правительствами стран 
  Европы, Северной и Южной Америки<sup>3</sup>. Тем не менее, несмотря на солидную 
  источниковую основу и методологическую новизну постсоветских исследований в 
  них отсутствует анализ историософской и исторической мысли казаков-эмигрантов. 
  Историографические проекты лидеров различных течений казачьего зарубежья оказались 
  вне фокуса исследовательского интереса российских ученых. Как следствие и допущенное 
  некоторыми авторами упрощенчество в оценках политической борьбы казачьих эмигрантских 
  объединений<sup>4</sup>. <br>
  <br>
  Если же говорить об исследованиях по истории участия казачества в революции 
  и Гражданской войне (в данном случае нас интересуют лишь историографические 
  сюжеты), то даже наиболее серьезные и содержательные работы российских ученых 
  постсоветского периода продолжают оставаться под влиянием советского историографического 
  дискурса. В рамках советского дискурса все многообразие подходов, концепций 
  и оценок роли казачества в событиях 1917-1922 гг., существовавших в исторических 
  исследованиях представителей Зарубежной России, рассматривается как единое направление 
  - «эмигрантская историография»<sup>5</sup>. Подобного рода «генерализации» кажутся 
  нам неприемлемыми как по теоретико-методологическим причинам (насколько правомерно 
  объединять рамках одного направления представителей разных научных школ!?), 
  так и по соображениям содержательным. Само понять «эмигрантской историографии» 
  представляется во многом искусственно сконструированным и политизированным, 
  базирующимся на оппозиции «советская историческая наука - несоветская (антисоветская, 
  белогвардейская и пр.)». Куда более корректно было бы говорить о научных школах 
  направлениях первой, второй и третьей волн русской эмиграции (равно как и эмиграции 
  этнической), либо вести речь о различных идейно-политических течениях, которые 
  помимо собственно политических доктрин предлагали оригинальные историографические 
  проекты. Очевидно, что работы военных историков А.А. Зайцова и Н.Н. Головина, 
  исторические исследования полумемуарного характера П.Н. Краснова, И.Г. Акулинина, 
  А.И. Деникина, историко-правовое сочинение С.Г. Сватикова, проект истории казачества 
  с целью обоснования создания национального государства «Казакия» Т.М. Старикова 
  или комплексное исследование по истории Зарубежной России М. Раева, в котором 
  рассматриваются и сюжеты по истории казачьей эмиграции, нельзя механически подогнать 
  под одно «направление»<sup>6</sup>. Слишком различаются общественно-политические, 
  теоретико- методологические воззрения перечисленных выше авторов и слишком разного 
  научного качества исторические исследования они предлагают своим читателям. 
  Нельзя сбрасывать со счетов и фактор аудитории, к которой исследования представителей 
  «эмигрантской историографии» обращены. Если Сватиков залогом успеха в изучении 
  казачьей истории видел «отсутствие узкого национализма и глубокий научный интерес», 
  то Стариков, готовя «План работ по истории казачества», имел в виду задачу прямо 
  противоположную — обоснование казачьей государственности и идеологии казачьего 
  национализма. В одном случаем речь можно вести об академической науке, в другом 
  о презентивной историографии. <br>
  <br>
  Попытку отказаться от «генерализаций» в оценках историографии истории казачества 
  в Зарубежной России предпринял ростовский историк А.В. Венков. Однако его типологию 
  эмигрантской историографии нельзя, на наш взгляд, признать вполне удовлетворительной. 
  Согласно его выводам, «с окончанием гражданской войны и появлением первых белоэмигрантских 
  сочинений по ее истории стали очевидны три течения в рассматриваемом вопросе 
  о донском казачестве в гражданской войне». Эти три течения, по мнению исследователя, 
  - монархическое, «централисты» кадетского толка и «самостийники» эсеровского 
  толка<sup>7</sup>. Во-первых, говоря об исторических сочинениях представителей 
  Зарубежной России, было бы столь же неверно (как в случае и с «эмигрантской» 
  историографией) говорить об историографии «белоэмигрантской». Уместно ли рассматривать 
  в качестве белоэмигрантов казаков-самостийников на основании одного лишь их 
  участия в едином антибольшевицком фронте на юге России в 1917-1920 гг. Сами 
  казаки-самостийники не рассматривали себя в качестве защитников идеалов Белого 
  дела: «Сейчас уже для всех нас является бесспорным то, что второю из причин 
  наших неудач в прошлом, если не главною был неудачный выбор союзников (Добрармия) 
  и война с теми, кто мог быть союзником действительным (Украйна)», - писали в 
  своей программной статье лидеры «Вольноказачьего движения»<sup>8</sup>. Продуктивно 
  ли в начале 1990-х гг. приписывать к сторонникам Белого дела тех, кто отрекся 
  от него в середине 1920-х гг.? По мнению же генерала И.Ф. Быкадорова (видного 
  участника антибольшевицкого движения и одного из идеологов эмигрантского самостийничества), 
  Белое движение отличалось «худосочием», а союз казаков и белогвардейцев был 
  вынужденным, ситуативным и искусственным<sup>9</sup>. Во-вторых, Венков совершенно 
  необоснованно отождествил самостийничество и эсеровские настроения. По словам 
  упомянутого выше Быкадорова, «никакая власть российская (центральная) не сможет 
  быть благой для казачества, какая бы она ни была: монархическая, кадетская (милюковская), 
  эсеровская (Керенского или Чернова) или евразийская»<sup>10</sup>. В качестве 
  одного из периодов противоборства казачества и Российского государства авторы 
  очерка «Трагедия казачества» (написанного в духе самостийнической «партийности») 
  рассматривали борьбу казаков с «русской революционной демократией» (представленную 
  в значительной степени и эсерами) в феврале-октябре 1917 г.<sup>11</sup><br>
  <br>
  Добавим сюда и тот факт, что осмысление истории казаков в период гражданского 
  противоборства вели представители не только русской, но и украинской эмиграции. 
  Свои взгляды на «возрождение» казачества Украины и на историю Украинского вольного 
  казачества в 1917-1918 гг. высказывали представители как «гетманского», так 
  и петлюровского лагеря. То есть казачья тема не была монополией исключительно 
  белоэмигрантов<sup>12</sup>. <br>
  <br>
  В настоящей статье мы хотели бы обратиться к историографическому проекту, разработанному 
  лидерами «Вольноказачьего движения» («казакийцами»), обратившимися с конца 1920-х 
  гг. к истории для «конструирования» новой национальной общности - «казаки», 
  «казачий народ». Перед нами стоят разноплановые задачи. Необходимо определить 
  вклад историков - участников «Вольноказачьего движения» в изучение различных 
  аспектов истории российского казачества и прежде всего событий «красной смуты». 
  Их концепции представляют интерес не только как факты истории общественно-политической 
  мысли. Тезисы, выдвинутые в разное время участниками «Вольноказачьего движения» 
  оказывали и продолжают оказывать влияние на академическую науку<sup>13</sup>. 
  В этой связи чрезвычайно важным представляется определение научной ценности 
  неакадемических (в нашем случае националистических презентивных) сочинений по 
  истории. Вторая задача - научно-этическая. Интеллектуальное «конструирование» 
  этнических и национальных общностей, создание националистической идеологии посредством 
  обращения к истории поднимает вопросы об этической ответственности и научной 
  честности историка в работе над сбором и интерпретацией источников и фактов. 
  <br>
  <br>
  «Вольноказачье движение» стало самостоятельной общественно-политической силой 
  казачьего зарубежья во второй половине 1920-х гг. Группа известных донских и 
  кубанских казачьих общественных, военных деятелей, публицистов (И.А. Билый, 
  И.Ф. Быкадоров, Т.М. Стариков, М.Ф. Фролов и др.), вышедших в 1927 г. из «Общеказачьего 
  сельскохозяйственного союза» и редколлегии «демократического казачьего органа» 
  «Путь казачества», «творчески развили» идею атамана Всевеликого Войска Донского 
  П.Н. Краснова о необходимости «народной казачьей», а не «классовой» войны против 
  большевизма<sup>14</sup>. Лидеры нового движения провозгласили своей целью создание 
  независимого федеративного государства Казакии на основе идеологии казачьего 
  национализма. По словам Старикова, от целей «Вольноказачьего движения» веяло 
  «неприкрытой самостийностью»<sup>15</sup>. О том насколько радикальными были 
  цели архитекторов Казакии, свидетельствует их программное заявление, опубликованное 
  в первом номере журнала «Вольное казачество - Вильне козацтво». «Чем больше 
  казаки задумывались над подобными вопросами... «кто мы?» и «что мы?» - тем вернее 
  начали приходить к мысли, что достаточно уже нам быть орудием в руках других, 
  что свои силы и свои средства нужно тратить для достижения своих целей. И неуклонно 
  совершается процесс освобождения и отхода из под фактической и идеологической 
  связи и зависимости от былых «вождей» и разных российских политических партий, 
  организаций и группировок и той части казачества, которая вольно или невольно 
  в такой зависимости и связи еще была»<sup>16</sup>. В существовании казачьего 
  государства «казакийцы» видели некий третий путь преодоления «русской смуты» 
  (не «белый» и не «красный»), гарантирующий и от реставрации Российской империи, 
  и от большевицкого «нового мира». Исследование истоков, причин и этапов развития 
  казачьего национализма не является основной задачей данного исследования<sup>17</sup>. 
  Тем не менее, без рассмотрения некоторых особенностей «казачьей идеи» историографический 
  (как впрочем, и исторический) анализ воззрений лидеров «Вольноказачьего движения» 
  был бы неполным. Казачий национализм как целостная идеологическая система, имеющая 
  свои организационные структуры — порождение эмиграции. В дореволюционной России 
  не существовало ни сепаратистских, ни националистических казачьих партий или 
  движений, сравнимых с такими политическими объединениями «окраин» Российской 
  империи, как «Дашнакцутюн», «Мусават», УСДРП, социал-федералисты Грузии. Политическая 
  «программа» казачьей группы в Государственной Думе не шла дальше умеренного 
  автономизма. Складывалась парадоксальная ситуация — националистические (как 
  минимум партикуляристские) настроения в казачьей среде существовали, но на партийно-политическом 
  и идейно-теоретическом (публицистика не в счет) уровне не были выражены. <br>
  <br>
  Февральские и в особенности октябрьские события 1917 г. и ставшая их следствием 
  «фрагментация» империи, способствовали складыванию на ее «окраинах» не только 
  независимых или квазинезависимых государственных образований, но и своеобразной 
  оппозиции: столица (как «территория войны и революции») противопоставлялась 
  провинции («как территории мира и стабильности). Постреволюционные лидеры казачьих 
  областей предприняли попытки «отгородиться» от столичной «смуты». «Сама жизнь, 
  - писал атаман Всевеликого Войска Донского А.П. Богаевский, - заставила казаков 
  отделить свои края от советской России и объявить их самостоятельными государственными 
  образованиями»<sup>18</sup>. В то же время и Богаевский, и другие деятели новых 
  государств рассматривали эту сецессию как явление временное, видя будущее казачьих 
  областей как автономий в составе России. Тем не менее, казачье государственное 
  строительство сопровождалось ростом партикуляризма и сепаратизма. Эти настроения 
  получили «союзника» в лице генералитета Вооруженных сил на Юге России, выступавших 
  за беспрекословное следование принципу «единой и неделимой России» и видящих 
  в казаках лишь хорошее средство для его реализации. Генерал П.Н. Врангель, размышляя 
  о причинах неудач Белого дела, приходил к следующим выводам: «Лишь немного не 
  хватило (белым войскам - С.М.), чтобы начать драться с казаками, которые составляли 
  половину нашей армии и кровью своей на полях сражений спаяли связь с регулярными 
  частями»<sup>19</sup>. В годы Гражданской войны появилось выражение - «пограничная 
  болезнь казаков». Эта «болезнь» сыграла роковую роль в судьбе антибольшевицкого 
  фронта. В ходе Гражданской войны казаки рассматривали свои области как островки 
  безопасности от большевизма и отказывались переходить границу и вести борьбу 
  с частями Красной армии за пределами своих территорий. Весьма ярким проявлением 
  «пограничного» «недуга» является выступление на Войсковом Круге Всевеликого 
  Войска Донского в августе 1918 г. одного из казачьих атаманов: «Россия? Конечно, 
  держава была порядочная, а ныне произошла в низость, ну и пущай... у нас своих 
  делов не мало, собственных... Наш царь-Дон!» В другом выступлении звучал призыв 
  «защищать донскую землю, но не защищать царскую корону, не навязывать России 
  когти царского орла»<sup>20</sup>. Неудача в реализации проектов по созданию 
  казачьих государств, поражение в Гражданской войне, эмиграция поставили перед 
  казаками острые вопросы: «С кем и за что мы воевали, ради чего сложили казаки 
  столько своих голов и пролили реки крови; кто были наши подлинные союзники и 
  кто настоящие враги, и почему борьбу мы проиграли?»<sup>21</sup><br>
  <br>
  Противопоставляя «территорию войны и революции» (Москву и Петроград) «территории 
  мира и стабильности» (Дон, Кубань и другие казачьи области), лидеры «Вольноказачьего 
  движения» в эмиграции возродили по сути дела «пограничную болезнь» казаков времен 
  Гражданской войны, став на позиции радикальной самостийности. Для реализации 
  цели по созданию Казакии лидеры нового движения считали н обходимым «переориентировать 
  Казачество на самое себя, внести ясность в казачий вопрос, поставить этот вопрос, 
  вопрос об исторической роли казачества и его исторической судьбе как задач саму 
  по себе, найти место и роль казачества в будущем, бороться взглядом на Казачество 
  с точки зрения чужих интересов... поставить вопрос о восстановлении государственного 
  бытия казачества... пробудить жажду к жизни для... развития своего национального 
  &quot;Я&quot;»<sup>22</sup>. <br>
  <br>
  История казачества рассматривалась лидерами «Вольноказачьего движения» как важнейшая 
  составная часть казачьего «национального возрождения». По мнению Быкадорова, 
  «возрождение каждого народа начиналось, или было тесно связано с изучением истории 
  этого народа самим этим народом. Изучая свое историческое прошлое, народ осознала 
  себя, осознавал пройденные этапы своего исторического развития, познавал духовные 
  силы свои, предугадывал этапы своего будущего развития»<sup>23</sup>. Значительное 
  место в изучении казачьей истории для торжества будущей Казакии отводилось исследованию 
  истории русской революции и Гражданской войны в казачьих областях Дона, Кубани, 
  Терека и Урала. <br>
  <br>
  Лидеры «Вольноказачьего движения» не были академическими историками. Этот факт 
  неоднократно становился основой для их критики со стороны профессиональных ученых. 
  В одной из своих публикаций П.Н. Милюков упрекнул донского атамана П.Х. Попова 
  за то, что последний «ссылается на отсебятину импровизированного казачьего «историка» 
  генерала Быкадорова (которому самому стало совестно своих исторических открытий)»<sup>24</sup>. 
  Свою рецензию на труд Быкадорова, известный русский историк С.Г. Пушкарев назвал 
  «&quot;История&quot; казачества». По словам Пушкарева, центральной идеей работы 
  Быкадорова является «идея особого казачьего народа, возникновение которого «никакой 
  связи, никакого отношения к Руси Северо-Восточной (Московской) не имеет». При 
  этом, однако, г. Быкадоров не только пишет свои «сочинения» на языке столь враждебной 
  и ненавистной ему русской народности, но и высказывается против стремлений к 
  «образованию своего особого национального языка, что со стороны стремящихся 
  к тому является ошибкой и заблуждением»<sup>25</sup>. Известный политический 
  и общественный деятель казачьего зарубежья В.А. Харламов (историк по образованию) 
  был также резок в своих суждениях об исторической ценности трудов лидеров «Вольноказачьего 
  движения». «...Нашелся новоявленный ученый ген[ерал] И. Быкадо- ров, который... 
  открыл казачью нацию, наградив ее особыми предками - бродниками. Это научное 
  открытие нужно было вольным казакам для того, чтобы потребовать для любой казачьей 
  нации, на основании принципа самоопределения народов особого казачьего государства»<sup>26</sup>. 
  <br>
  <br>
  Тем не менее, характеризовать лидеров «Вольноказачьего движения» исключительно 
  как некомпетентных политизированных дилетантов было бы неверно. По словам генерала 
  Старикова, «перед Великой войной (Первой мировой — С.М.) почти со дня окончания 
  училища я все свободное время от службы и войны (кстати, продолжавшейся для 
  меня 6 ? лет) посвятил изучению истории Казачества. Имел весьма солидную библиотеку. 
  Несколько кратких исторических очерков были напечатаны в периодической печати. 
  Ряд очерков было заготовлено... Но все мои работы по истории и вся ценная библиотека 
  была уничтожена большевиками в моей родной станице. Я едва пережил этот тяжкий 
  удар. В Праге оказалась полная возможность возобновить работы по истории...»<sup>27</sup> 
  В 1923 г. Старикову было предложено сотрудничать в журнале «Казачий путь», чтобы 
  освещать историю антибольшевицких восстаний на Дону. «Вы лучше, чем кто-либо 
  справитесь с ней (с темой — С.М.) ...Я оставил эту тему за Вами» - писал Старикову 
  председатель Донской исторической комиссии в Праге Харламов<sup>28</sup>. Впоследствии 
  политические пути Харламова и Старикова разошлись, но, несмотря на расхождения 
  во взглядах, Стариков продолжал считаться (в том числе и его оппонентами) одним 
  из лучших знатоков антибольшевицких казачьих восстаний на Дону, равно как и 
  одним из лучших военных историков Гражданской войны на Юге России. К сожалению, 
  большая часть из задуманного и написанного Стариковым не была опубликована. 
  Смерть 28 ноября (11 декабря) 1934 г. помешала автору реализовать масштабный 
  исследовательский проект. Генералом был составлен план по написанию обобщающего 
  труда по истории российского казачества, в котором предложил свою оригинальную 
  концепцию. «...мне кажется, что «История всех казачьих войск» была бы для современного 
  момента наиболее ценной. Такой работы на русском языке нет. И не только русский 
  человек, но и сами казаки за исключением, может быть, нескольких человек такой 
  истории не знают», - делал вывод Стариков<sup>29</sup>. В плане по изучению 
  истории казаков Стариков значительное место отводил событиям революции и Гражданской 
  войны. Он предполагал издание собственных воспоминаний из пяти частей «То, чему 
  я был свидетель», охватывающих события от октябрьского переворота 1917 г. до 
  окончания Гражданской войны на Юге России. Некоторые из идей предполагаемых 
  воспоминаний нашли отражение в очерке генерала «Трагедия казачества», подготовленном 
  совместно с генералом В.И. Сидориным и опубликованном в журнале «Вольное казачество 
  - Вильне козацтво»<sup>30</sup>.<br>
  <br>
  «Исторический опыт» был и у другого генерала - Быкадорова. Ему было присвоено 
  звание почетного члена императорского Археологического общества за нахождение 
  места переправы скифов через Дон. По словам А.К. Ленивова, «уже в академии молодой 
  офицер И.Ф. Быкадоров обратил на себя внимание в связи с составленным им конспектом 
  по всеобщей истории согласно курсу занятий в Академии. Этот конспект пользовался 
  заслуженным успехом среди офицеров учебных классов и получил также одобрение 
  профессоров Академии. Заслуженное признание получили и труды Быкадорова по военной 
  истории, в частности очерк, посвященный участию казаков в войнах против Наполеона<sup>31</sup>. 
  По словам самого Быкадорова, им «начато еще в 1912 г. изыскание и собрание материалов 
  в условиях, далеко отличавшихся от эмигрантских». <br>
  <br>
  Уже в эмиграции будущие лидеры «Вольноказачьего движения» основали Общество 
  изучения казачества, на заседаниях которого в течение двух лет регулярно читались 
  доклады по актуальным проблемам истории «степных рыцарей»<sup>32</sup>. Тем 
  не менее, несмотря на отмеченные выше достижения, у «казакийцев» отсутствовала 
  серьезная академическая «историческая» подготовка, что не могло не отразиться 
  на общем уровне их исследований. В то же время мы не должны забывать и тот факт, 
  что во главу угла «казакийцы» ставили не научную объективность и беспристрастность 
  (хотя неоднократно апеллировали к ним), а политическую цель — самоопределение 
  казачьего народа. <br>
  <br>
  Исследование роли казачества в событиях «красной смуты» занимает особое место 
  в трудах «казакийцев». С точки зрения авторов очерка «Трагедия казачества» (своеобразной 
  историософской Библии «Вольного казачества), «объективное и подробное изучение 
  этого периода (1917-1920 гг. - С.М.) казачье-русских взаимоотношений представляет 
  большой интерес, как для самого Казачества, так и для других народов, и особенно 
  для тех, которые продолжают еще борьбу за свое освобождение от московского владычества. 
  Без сомнения, это изучение может представлять большой и даже прагматический 
  интерес и для тех освободившихся от русского владычества народов, которые видят 
  перед собой постоянную фактическую угрозу со стороны московского империализма. 
  Москва ведь всегда легко придумывала те или иные «легальные» поводы для начала 
  войны с соседями»<sup>33</sup>. В процитированном выше тексте ключевым словосочетание 
  - «казачье-русские отношения». С позиций «казакийской историософии» борьба казачества 
  Дона, Кубани, Терека и Урала с большевиками в 1917-1920 гг. является всего лишь 
  очередным этапом борьбы «вольных» казаков с Русским (Российским) государством 
  и русским народом, зараженным имперским духом. Согласно выводам очерка «Трагедия 
  казачества», в 1920 г. казаки проиграли «великую битву с Россией», которая имела 
  три этапа: борьба с русским государством, его народом, «с его культурой и некультурностью»; 
  борьба с «русской революционной демократией», русскими партиями в лице Временного 
  Правительства; с большевицкой Россией, «с русскими большевистскими армиями»<sup>34</sup>. 
  Т.е. для «казакийцев» не было существенной разницы между императором, Керенским 
  и Лениным. По мнению авторов «Трагедии казачества», русская демократия «продолжала 
  многовековую традицию «душителей» в национальном вопросе»<sup>35</sup>. Однако 
  вождь большевиков уничтожил казачью государственность, что не удавалось сделать 
  полностью ни императорскому, ни Временному правительствам (Стариков)<sup>36</sup>. 
  <br>
  <br>
  Исследование общеисторических и методологических взглядов участников «Вольноказачьего 
  движения» не является задачей настоящего исследования. Между тем определение 
  некоторых общих схем российской истории и исторической роли казачества, представленных 
  «казакийцами», кажется нам обоснованным. Без этого их трактовки событий 1917-1920 
  гг. не будут восприниматься контекстуально. «Казачество - основы особой государственности 
  Востока Европы, противоположные по своему содержанию русским», - писал Быкадоров<sup>37</sup>. 
  В чем же, на взгляд «казакийцев», заключалась особенность этих «основ»? Казачьи 
  образования (Войско Донское, Терское, Яицкое) были «демократическими республиками, 
  управляемыми на основе народного волеизъявления» (Ленивов)<sup>38</sup>, «государствами, 
  основанными на началах равенства и свободы» (Стариков)<sup>39</sup>. До первой 
  четверти XVIII в. (подчинения Войска Донского военной коллегии), казаки, согласно 
  выводам участников «Вольноказачьего движения», вели перманентную борьбу (с переменным 
  успехом) с колониальной политикой Российского государства за свои права и «вольности». 
  Потеряв свою самостоятельность в эпоху Петра Великого, казаки продолжили в различных 
  формах противоборство имперской политике, добившись восстановления государственности 
  в 1917 г.<sup>40</sup><br>
  <br>
  Историки-«казакийцы» единодушно отмечали решающую роль казачества в защите южных 
  рубежей России, укреплении ее оборонной мощи и расширении границ. Быкадоров 
  рассматривал казачество в качестве своеобразного щита восточноевропейской цивилизации 
  на пути экспансии кочевых степных народов, а позднее Османской империи. «Полтавская 
  победа (1709) спасла Русскую Империю от шведов, Бородинское сражение с пожертвованием 
  Москвой спасло ее от Наполеона, но значение победы Донского войска в Азовском 
  сидении для всего Востока Европы шире и глубже, т.к. победой в Азовском сидении 
  Донское Войско спасло бытие свое и бытие Московского государства и возможность 
  образования Русской империи»<sup>41</sup>. По мнению того же Быкадорова, определяющую 
  роль сыграли казаки и в окончательной победе Антанты над Германией и центральными 
  державами в годы Первой мировой войны<sup>42</sup>. Стариков подчеркивал также 
  значительную роль казачества в «охране внутреннего порядка» империи: «Но русское 
  правительство в конце концов не ограничилось только военной службой казаков... 
  В 1905 и 6 годах (так в авторской рукописи — С.М.) казаки усмирили восставших 
  крестьян и рабочих и остановили т[аким] о[бразом] начавшуюся революцию. За это 
  не только крестьяне и рабочие, но и вся русская прогрессивная интеллигенция 
  стала относиться к ним враждебно»<sup>43</sup>. <br>
  <br>
  Но, несмотря на значительные военные и полицейские заслуги, казаки были подвергнуты 
  «русификации». Утрата казачьего «национального Я» происходила не только вследствие 
  политического давления со стороны империи. По мнению Ш. Балинова, экономическая 
  политика империи была направлена на эксплуатацию природных богатств казачьих 
  областей. «А политика Москвы, а затем Петербурга в отношении казачества носила 
  все признаки колонизации. Казачьей кровью завоеванные казачьи земли и естественные 
  богатства объявлялись собственностью государства и царями и царицами произвольно 
  жаловались своим фаворитам и отдельным «преданным» себе казакам, чем создавался 
  на казачьей земле класс помещиков и казачьих «старшин», что являлись могучими 
  пособниками в деле подчинения Казачества Москве; а с другой стороны поощряемые, 
  поддерживаемые политикой правительства на казачьи земли «полезли вереницы русских 
  пахарей», создавая на казачьих территориях т.н. «временные поселения», на них 
  укреплялись, постепенно создавая свое &quot;большинство&quot;»<sup>44</sup>. 
  «Денационализации» казачества способствовала, согласно Балинову, и урбанизация 
  (шире говоря, экономическая модернизация): «...Ростов, Нахичевань, Таганрог, 
  имеющие огромное экономическое значение... остаются городами неказачьими. В 
  этих городах, т.е. в руках чужеродных элементов была сосредоточена вся экономическая, 
  торговая, промышленная жизнь края. Через их руки вывозились все предметы тяжелого 
  физического труда станичного казачьего населения...»<sup>45</sup><br>
  <br>
  Сквозь призму «денационализации» казачества лидеры «Вольноказачьего движения» 
  рассматривали и «великие реформы Александра II. По мнению Старикова, «все законы 
  и усилия Правительства были направлены (в 1860-70-е гг.) к тому, чтобы объединить 
  и слить казачье население с прочим населением России, разрешению неказакам покупать 
  имущество и селиться в казачьих краях, зачисляться в казаки иногородним, отставным 
  солдатам и офицерам, а казакам свободно выходить из казачьего звания»<sup>46</sup>. 
  <br>
  <br>
  Противопоставляя казачество и Российское государство, участники «вольноказачьего» 
  движения, как и в случае с концепцией «казаки-народ» вступали в противоречия 
  сами с собой. Никто из историков-«казакийцев» не разрешил следующее логическое 
  противоречие. Войско Донское, согласно их выводам, было внешнеполитическим соперником 
  Московского государства. Зачем в таком случае казаки всячески содействовали 
  продвижению и утверждению русских государей на южных рубежах? «...казачье независимое 
  государство, взявши важнейшую вражескую крепость (турецкий Азов в 1637 г. - 
  С.М.), сейчас же отдает ее другому независимому государству и притом, согласно 
  утверждениям г. Быкадорова, государству враждебному»<sup>47</sup>. Почему покоренные 
  и «денационализированные» Москвой (а затем и Петербургом) казаки приняли участие 
  во всех войнах и полицейских мероприятиях, которые вела империя? Увы, но на 
  эти вопросы развернутых ответов получено не было. <br>
  <br>
  По мнению «казакийцев», исторически казачество было антагонистом не только Российского 
  государства, но и общества. По словам Старикова, «одно обстоятельство резко 
  бросается в глаза. Это то, что русское общество не знало казаков. В большинстве 
  случаев казаков представляли как людей, лишь сидящих на коне с большим чубом 
  и длинной пикой. После 1905 года к этой картинке прибавилась нагайка. Вот и 
  все, что знали о казаках»<sup>48</sup>. <br>
  <br>
  Большевизм как политическое течение «казакийцы» также считали порождением русской 
  истории. На наш взгляд, в оценке большевизма как «почвенного» явления «казакийцы» 
  парадоксальным образом сходились со своими оппонентами — «сменовеховцами». Показательными 
  в этом плане являются размышления Старикова: «Многие утверждают, что большевизм 
  привезен в Россию из Германии в запломбированном вагоне. Но это было бы слишком 
  просто. В действительности привезен только факел. Костер же был приготовлен 
  нашей (sic!) русской действительностью, нашими русскими исторически сложившимися 
  условиями жизни». Первичными одинаково огромными причинами возникновения русского 
  большевизма и форм его проявления были бедность и темнота русского народа»<sup>49</sup>. 
  Отсюда и восприимчивость русского народа, согласно «вольноказачьей» историософии, 
  к большевицким идеям. «Большевистскую власть без сопротивления, без какой-либо 
  борьбы, добровольно принял единственный народ бывш[ей] России - великорусский; 
  он принял, как программу, как свою идеологию, и большевизм. Борьба против большевистской 
  власти и большевизма, борьба вооруженная начиналась там, где кончалась великорусская 
  или начиналась этнографическая граница других народов»<sup>50</sup>. <br>
  <br>
  Следствием отказа от «централистского» подхода к русской (российской) истории 
  стали три основополагающие теоретические установки для «казакийской» концепции 
  истории революции и Гражданской войны: русофобия; казачья государственность 
  как высшая политическая ценность и фактор, детерминирующий политическое поведение 
  казачества в 1917-1920 гг.; отрицание собственно концепции «Гражданская война» 
  и сведение событий 1917- 1920 гг. к формату противоборства двух народов казачьего 
  и русского, придание Гражданской войне характера внешнего противоборства.<br>
  <br>
  Своеобразным стержнем «казакийской» концепции революции и Гражданской войны 
  стала идея о возврате казачьих областей в 1917 г. к «Золотому веку», которым, 
  по мысли идеологов казачьего национализма был допетровский период (XVI - начало 
  XVIII вв.), период существования «вольного казачества», не приносившего присяги 
  на верность службы московским государям и основанного на демократических принципах 
  Войска Донского. Избрание в июне 1917 г. атаманом А.М. Каледина, возрождение 
  в новой форме казачьего Круга рассматривалось «казакийцами» как «возрождение» 
  казачьей государственности и казачьей демократии. <br>
  <br>
  Отсюда же идеологи «Вольного казачества» обозначали и более широкий круг противников 
  «казачьего дела» (в отличие от сторонников Белого дела). Среди своих противников 
  «казакийцы» определяли большевиков, русскую «революционную демократию» и Добровольческую 
  армию (и Белое дело вообще). В этой связи «казакийцы» проводили достаточно жесткую 
  демаркационную линию между антибольшевицким движением и Белым делом, утверждая, 
  что политический союз Добровольческой армии и казаков был неестественным союзом 
  разнонаправленных сил, одна из которых боролась за торжество казачьей государственности, 
  другая за единую и неделимую Россию. Таким образом антибольшевик и белогвардеец 
  (даже если по формальным признакам они были объединены службой в Вооруженных 
  силах на Юге России) не были для лидеров «Вольноказачьего движения» тождественными 
  понятиями. <br>
  <br>
  «Войну против большевиков и против тех, кто за ними шел, или кого они против 
  Казачьих краев воевать посылали, вели не отдельные группы или партии, а все 
  казачество, весь казачий народ. В то время как для различных русских антибольшевиков 
  эта борьба была внутренним спором разных партий и группировок за государственную 
  власть и за тот или иной социальный строй, то есть войной гражданской, для нас 
  эта борьба была войной внешней. Не внутренний спор, не свое домашнее дело решали 
  казаки с большевиками в течение почти трех лет на поле брани. Это была внешняя 
  оборонительная война наших Республик против нападавшего северного государства. 
  Казачество предпочитало самостоятельное государственное существование своих 
  краев включению в красное Московское царство. Таким образом, борясь с Деникиным 
  или Колчаком, Московская советская власть боролась против контрреволюции, замиряла 
  внутренний бунт. Идя же войной на Дон и Кубань (точно также как на Украину, 
  Грузию и др.), та же Москва шла завоевывать эти земли и народы, провозгласившие 
  к тому времени свою государственную самостоятельность», - констатировали авторы 
  очерка «Эмиграция казачья и эмиграция русская»<sup>51</sup>. С точки зрения 
  лидеров «Вольноказачьего движения» борьба с большевиками была для казаков борьбой 
  не против социально-экономических и политических экспериментов партии Ленина, 
  а борьбой за возрожденную казачью государственность, уничтоженную Петром Великим, 
  а также казачьи «вольности» (выборность атаманов, внутренняя демократия). По 
  словам Балинова, определяющим фактором политического поведения казачества в 
  1917-1920 гг. была защита казаками Всевеликого Войска Донского как «самостоятельного 
  государства, основанного на началах народоправства»<sup>52</sup>. Именно неприятие 
  большевицкими лидерами начал народоправства и стало причиной того, что «...немедленно 
  после захвата власти большевиками в Петрограде казачьи земли объявили свою независимость, 
  начав оборонительную войну. Война продолжалась (на казачьих же территориях) 
  с октября 1917 г. до апреля 1920 г.»<sup>53</sup>. <br>
  <br>
  Вместе с тем, «казакийцы» не видели принципиальной разницы между большевиками 
  и Временным правительством или «русской революционной демократией», поскольку 
  и те, и другие олицетворяли Русскую державу. Более того, Временное правительство 
  проводило политику, направленную против казачьего партикуляризма, что трактовалось 
  «казакийцами» как покушение на возрождающуюся казачью государственность. «Русская 
  демократия, революционная и просто демократия не сумела наладить... сносное 
  государственное управление, восстановить порядок, наладить продовольствие населения 
  и армии, прекратить агитацию в тылу и в армии, со стороны своих более левых 
  товарищей-большевиков и левых соц[иалистов]-рев[олюционеров]. В то же время 
  она весьма последовательно вела борьбу не только с возрождающейся казачьей государственностью, 
  но и с украинской, белорусской и т.д. освободительными движениями (так в тексте 
  — С.М.). Александр Керенский в сентябре 1917 года мобилизовал против казаков 
  два русских военных округа, натравлял на казаков русскую крестьян[скую] и солдат[скую] 
  массу, а в ноябре мес[яце] того же года просил тех же казаков спасти Временное 
  правительство и русскую революционную демократию от другой, более левой части 
  русской же революционной демократии — большевиков... То же самое делали и провинциальные 
  русские революционные демократы». По мнению авторов «Трагедии казачества», русская 
  революционная демократия имела слабую политическую волю, поскольку «...с такой 
  позорной легкостью и малодушием передала ее (власть - С.М.) в руки более левых 
  своих товарищей; она уже имела в своих руках Всероссийское Учредительное Собрание 
  и не сумела защитить его; она пробовала образовать на Волге фронт Учредительного 
  Собрания, но не сумела удержать его против большевиков, хотя и опиралась на 
  40-ка тысячный корпус чехословацкий легионеров (орфография сохранена - С.М.)». 
  Авторы очерка попытались сравнить эффективность казачьего антибольшевицкого 
  фронта и борьбы «демократической контрреволюции» на Волге и на Урале. «А в это 
  время (лето-осень 1918 г.) одно только Донское Войско, освободившее свою землю 
  от пришлых большевиков, удерживало большевицкие московские войска на восьмисот 
  верстном фронте... Один казачий Дон мог выставить тогда далеко большие вооруженные 
  силы нежели русский демократический фронт на Волге... Если бы русский народ 
  хотел поддержать демократическое правление в России, а не большевиков, он мог 
  сделать это на Волге и на Урале. Донские казаки сумели воевать успешно в открытых 
  степях, а русские демократы не смогли защитить своих природных рубежей как Волга 
  и Уральские горы. Имеются серьезные основания для веры в то, что когда-либо 
  в будущем русский народ будет активно поддерживать демократическое устройство, 
  которого никогда не было у него за всю восьмисотлетнюю историю»<sup>54</sup>. 
  <br>
  <br>
  И наконец, последний соперник (он же временный и ситуативный союзник) казачества 
  — защитники Белого дела. Как мы уже отмечали выше, «казакийцы» подчеркивали 
  разную мотивацию защитников Всевеликого Войска Донского и единой и неделимой 
  России. «С Добрармией же у нас была лишь одна половина общей задачи: борьба 
  против большевиков, а дальше — расхождения — боролись мы за разное. Мало того, 
  Добрармия собственно и не хотела быть нашим союзником, она хотела стоять над 
  нами. В будущем мы очевидно, такой ошибки не сделаем. Мы боролись с большевиками 
  или вели спор с Добрармией не для для того, чтобы переменить &quot;барина&quot;»<sup>55</sup>. 
  По отношению к Белому делу Быкадоров использовал афоризм «худосочие». Причины 
  этого «худосочия» он видел в отсутствии у лидеров Белого дела «притягательных» 
  и «отталкивающих» идей. «Отталкивающая сила не была достаточно сильной, главное 
  резкой, в виде толчка; она была подобна прогрессивному параличу, который начался 
  задолго до октябрьского переворота. Относительно притягательной силы Доброармии 
  (орфография сохранена – С.М.) необходимо сказать, что главная идея ее - верность 
  союзникам, помощь в войне - после совершившегося (в феврале-октябре 1917 г. 
  - С.М.) не могла являться действенной. Да и в своем принципе собирания (принцип 
  добровольности) она содержала наследие 1917 г. После того как первое «белое 
  движение» — выступление Корнилова в августе — «скиксовалось» благодаря предательству 
  главы Временного Правительства А.Ф. Керенского, пассивности либеральной российской 
  буржуазии и враждебности революционной демократии, поднять вновь белое движение 
  было чрезвычайно трудно. Престиж, ореол генерала Корнилова и генерала Алексеева 
  после августовской неудачи пал и к ним не было полного доверия, не было веры 
  в успех их дела, в дело уже однажды проигранное. Почему, наконец, офицерство 
  должно было следовать смутному, глухому зову, о котором должно было больше догадываться, 
  по пребыванию на юге генералов Корнилова и Алексеева (о Деникине не слыхали, 
  он был мало кому тогда известен), не примеру не менее авторитетных генералов 
  Брусилова, Клембовского, Гутора и т.д.»<sup>56</sup> Белые армии, по мнению 
  Быкадорова, не имели также серьезной общественно-политической и социальной поддержки. 
  «Офицерство не могло быть главным элементом и фактором вышеуказанного дела, 
  - оно должно было быть задачей соответствующих классов, сословий - вообще здоровых 
  элементов Российского государства, но их не оказалось... Белые армии не могли 
  бы и месяца продержаться, если бы они не опирались на «белое движение». В действительности 
  они опирались на юге на казачество и горцев, на востоке на казачество и на чешских 
  легионеров, на западе на народы, осуществлявшие свое национальное самоопределение». 
  Результат - «противоестественный союз разных по содержанию элементов, соединение 
  противоречивых целей их»<sup>57</sup>. По мнению «казакийцев», этот противоестественный 
  союз завершился неудачей и для казачества и для Белого движения. П.Н. Кудинов 
  считал «русско-казачью» войну проигранной благодаря «русофильской политике нечестных 
  правителей, ловко ускользнувших из-под контроля Войскового Круга»<sup>58</sup>.<br>
  <br>
  «Казакийская» концепция истории революций и Гражданской войны по сути создавала 
  фантом казачьего политического единства перед лицом большевиков, русской революционной 
  демократии и Добровольческой армии. Лидеры «Вольноказачьего движения», по сути 
  дела, игнорировали раскол на красных и белых внутри самого казачества. Тем не 
  менее, «казакийцы» дали свое прочтение таких событий как «большевизация» казаков 
  в конце 1917-начале 1918 гг., их участие в составе частей Красной армии. По 
  мнению Старикова, «фронтовое казачество не поддержало тогда (октябрь 1917- январь 
  1918 гг. - С.М.) ни Круга, на Атамана. Оно считало, что московская власть признана 
  всем русским народом, что она является властью государственной, которой поэтому 
  следует подчиниться, как ранее подчинялись Врем[енному]. Правительству. К тому 
  же она несет свободу и равенство, т.е. за что всегда боролись. Но ошиблись фронтовики. 
  Московская власть обманула их. Она несла Казачеству тиранию. Поняв это, Дон 
  восстал. Собрался Войсковой Круг и снова объявил Дон самостоятельным государством»<sup>59</sup>. 
  Но против Советской власти весной 1918 г. восстало не все казачество. Некоторая 
  его часть вовсе не искала раскаяния и признания собственных политических ошибок, 
  а напротив, принимала участие в боевых действиях против Донской армии (в равной 
  степени и против Добровольческой) в рядах красных кавалерийских соединений Б.М. 
  Думенко, Ф.К. Миронова, С.М. Буденного. К сожалению, историки-«казакийцы» в 
  анализе причин внутриказачьего противостояния в 1917-1920 гг. ограничились констатацией 
  массового самообмана «красных казаков», поверивших большевистским проповедям 
  равенства, свободы, самоопределения народов<sup>60</sup>.<br>
  <br>
  Несмотря на политическую заданность своих публикаций, участники «Вольноказачьего 
  движения» собрали и ввели в оборот большое количество материала, относящегося 
  к событиям 1917-1920 гг. (Вешенское восстание 1919 г., взаимоотношения Донской 
  и Добровольческой армий, Вооруженных сил на Юге России и казачьих государственных 
  образований Кубани и Терека, казаков и горцев Северного Кавказа). <br>
  <br>
  Подведем итоги. Лидеры «Вольноказачьего движения» обратились к изучению казачьей 
  истории в эмиграции, потерпев неудачу в попытках создания самостоятельных государственных 
  образований казаков. Революция, Гражданская война, изгнание поставили перед 
  ними как и «перед всею Россией в целом и перед каждою ее частью проблему самопознания»<sup>61</sup>. 
  Отвечая на вопрос: «С кем и за что мы воевали, ради чего сложили казаки столько 
  своих голов и пролили столько крови?..»<sup>62</sup> «казакийцы» не ограничились 
  анализом событий 1917-1920 гг., а обратились к исследованию широкого круга тем 
  от происхождения казачества до участия казаков в Первой мировой и Гражданской 
  войнах. Обращение лидеров «Вольноказачьего движения» к истории проходило в условиях 
  всплеска интереса к казачеству и в эмиграции и в СССР. «Смысл» казачьей истории 
  лидеры «Вольноказачьего движения» видели в утверждении казачьего национального 
  «Я». Исторические исследования участников «Вольноказачьего движения» ставили 
  во главу угла формирование «казачьей национальной идеологии» и, по сути, были 
  «политикой, обращенной в прошлое». <br>
  <br>
  Заявив справедливые требования на отказ от «москвоцентризма» и марксистских 
  клише в изложении истории казаков, лидеры «Вольноказачьего движения» создали 
  немало собственных клише и штампов. Чего стоит один только тезис о единой политической 
  позиции казачества в 1917-1920 гг. и его борьбе с Русским государством. Какое 
  государство (политический режим) можно было с большим основанием считать «русским», 
  «Республику Советов» или «Доброволию»? Недостаток академической подготовки и 
  политическая заданность не позволили «вольным казакам» объективно и беспристрастно 
  интерпретировать источники. Следствием этого стали фактические ошибки в освещении 
  раннеказачьей истории, изображение ее в качестве некоего «Золотого века» «степного 
  рыцарства», а событий 1917 г., как попытки вернуться к этому «Золотому веку». 
  <br>
  <br>
  Тем не менее, нельзя не отметить, что «казакийцы» ввели в оборот новые исторические 
  источники, разносторонне исследовали те сюжеты казачьей истории, которые были 
  окутаны «дымкой загадочности» в Советской России. <br>
  <br>
  «Казакийцы» были активными участниками «русской смуты». В этой связи их наблюдения, 
  выводы, введенные в оборот материалы, относящиеся к событиям 1917-1920 гг., 
  представляют и на сегодняшний день огромную научную ценность для исследователей 
  революции и Гражданской войны в России. В особенности это касается истории казачьих 
  антибольшевицких восстаний на Дону, Кубани, Тереке, проблемы взаимоотношений 
  Донской и Добровольческой армий, казаков и горцев Северного Кавказа. Теоретическую 
  ценность представляет и попытка обосновать нетождественность понятий «антибольшевистский 
  фронт» и Белое движение (Белое дело), «антибольшевик» и «белогвардеец». Изучение 
  воззрений «казакийцев» чрезвычайно важно и актуально с точки зрения рассмотрения 
  вмешательства политических идей в историческую науку, анализа националистического 
  мифотворчества посредством обращения к истории, а также формирования общей картины 
  развития исторической мысли казачьей диаспоры (говоря шире Зарубежной России). 
