	<br><span class="text"><strong>В.А. 
        Соколов</strong><br>
  <br>
       <span class="header"><strong>ИСТОРИЯ КУБАНСКОГО ВОЙСКА</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 77-79</em>.<br>
  <br>
  <br>
  ...Кубань к 1914 году была населена казаками и иногородними, немцами и горскими 
  племенами. Теперь все жили мирно и строили и улучшали свою жизнь. На территории 
  Кубани имелось 2200 начаоьных школ, 183 высших школы, 120 классических гимназий 
  и реальных училищ. Это на 400 крупных населенных пунктов. Быстро развивалась 
  индустрия. К 1914 году было около 8000 фабрик и заводов, из них первое место 
  занимали мельницы и маслобойни. Молотьба хлеба производилась почти полностью 
  паровыми молотилками. Параллельно с земледелием развивалось скотоводство. В 
  крае насчитывалось до миллиона лошадей, более 2 миллионов рогатого скота, более 
  3 миллионов тонкорунных овец и других животных и масса птицы. Край цвел и развивался 
  даже во время войны 1914-1917 годов. <br>
  <br>
  Из истории Кубанского края<br>
  <br>
  Рада решила, что все жители, жившие на Кубани до 1914 года, приравнены к казакам. 
  Кубанскую область именовать Кубанским краем. Войсковое правительство - Краевым 
  правительством. Войсковую Раду — Краевой Радой. Краевая Рада приняла форму автономного 
  управления краем с комиссаром временного правительства, как передаточного органа. 
  Это положение временно действовало до образования Всероссийского Учредительного 
  Собрания. Дума также наметила кандидатов в Учредительное Собрание. <br>
  <br>
  11 октября 1917 года отпраздновали избрание атамана, вручили ему атаманскую 
  булаву, которую уже несколько десятков лет держали все выбранные атаманы. Выбрали 
  новое правительство. Рада, как вновь избранная, назначила свое заседание на 
  ноябрь, чтобы утвердить новое правительство. Сейчас оставалось старое. <br>
  <br>
  25 октября получили известия о перевороте. Решили — все пройдет. Большевиков 
  прогонят. Придут более энергичные люди, и все наладится. О лояльности с Совнаркомом 
  мысли не было. Об образовании борьбы тоже не думали. В начале ноября собралась 
  Законодательная Рада. Составила коалиционный президиум. Председатель — Рябовол, 
  секретарь - Рябзов, товарищ председателя — Султан Рахим Гирей, председатель 
  правления — Быч, внутренних дел — бывший комиссар Бардиж, юстиции — Султанов, 
  по военным делам — полковник генерального штаба Успенский (сейчас на фронте), 
  контролер — Скворцов. <br>
  <br>
  28 октября атаман всенародно объявил о принятии на себя всех прерогатив государственной 
  власти в области и непризнании большевистской власти в Москве. По Конституции, 
  мы не имели права сделать то же. Дошедшие из Петрограда и Москвы слухи о терроре 
  и утверждении власти большевиков пулеметами и пушками искоренили иллюзии о благоразумности 
  большевиков. Чтобы получить титул и мандат для борьбы, правительство решило 
  вновь собрать Краевую Раду. <br>
  <br>
  С фронта двигалась 39-ая дивизия, среди них распространялись листовки, призывающие 
  солдат поддержать иногородних против казаков. С фронта стали приходить и казаки, 
  но они не желали выступать против солдат: воевали вместе, последний сухарь делили. 
  Они требовали полный расчет за то, что им недодали на фронте и подняли скандал, 
  что их не желают понять старики-отцы. Быч пытался привлечь иногородних, привлек 
  одного — бывшего голову города Новороссийска. Создалась комиссия для переговоров 
  с иногородними, куда вошли Бардиж, Филимонов и Султан Рахим Гирей. <br>
  <br>
  Состоялся съезд иногородних, которые были недовольны, что Дума отказалась от 
  бессословного земства и только самостийно решила управлять с помощью коренного 
  населения. Была на съезде и фракция большевиков. Скобцову поручили переговорить 
  с делегатами съезда. Комиссия выработала соглашения. Вот они: <br>
  <br>
  1. До принятия Всероссийским Учредительным Собранием основных законов, население 
  Кубани избирает орган самоуправления в крае и в организующемся Юго-востоке.<br>
  <br>
  2. Создаются бессословные органы местного самоуправления. <br>
  <br>
  3. Состав станичной администрации с привлечением в нее иногородних. <br>
  <br>
  Второй пункт Дума не приняла, так как было много застрявших неизвестных.<br>
  <br>
  Декабрьская Дума была бурная. Выдвигались требования об увеличении прав для 
  иногородних, после чего правительство заявило Краевой Раде, что они складывают 
  ...свои полномочия из-за оппозиции фронтовиков и неясности их требований. Через 
  час фронтовики группой подошли к эстраде и потребовали открыть собрание. Снова 
  выступил полковник Роговец. В пылкой речи он извинился, что отцы их детей неправильно 
  поняли и просил Атамана и других взять обратно их отказ. Он прервал свою речь, 
  повернулся к фронтовикам и запел малоизвестную песню: «Ты, Кубань, ты наша Родина, 
  вековой наш богатырь». Так разрешился кризис между отцами и детьми. <br>
  <br>
  В это время пришло известие о гибели Атамана Караулова. По Восточно-Владикавказской 
  железной дороге двигались солдаты 39-ой дивизии. Они двигались с полным вооружением 
  для борьбы с контрреволюцией и против Атаманов Каледина и Филимонова. При дивизии 
  был создан большевистский революционный комитет с заданием устанавливать советскую 
  власть. <br>
  <br>
  Рада единодушно приняла идею бороться с большевиками и дала полномочия Правительству. 
  Было утверждено постоянное представительство в Раде: иногородних — 46, казаков 
  — 46 и горцев — 3. В представительстве: 5 казаков, 5 иногородних и 1 горец. 
  Войсковой Атаман и представитель правительства должны быть казаки. Срок оседлости 
  для права быть избранным — 2 года. Это принималось на совместно общих собраниях 
  Рады и съезда иногородних с 13 по 21 декабря. <br>
  <br>
  20 декабря Рада утвердила политическую программу. Она была демократическая, 
  и реально не могла тягаться с большевистски- ми посулами. В их воззваниях к 
  казакам были собраны все пожелания, о которых могли бы только подумать казаки. 
  Как лиса к ягнятам, подбирались большевики к народу. Казачье руководство объявлялось 
  подверженным сословным влияниям. Приводились астрономические цифры о собственных 
  землях у Каледина, Филимонова, Быча и других. <br>
  <br>
  Перед Рождеством начались спиртные бунты. Солдаты громили склады. Спирт находился 
  в больших Берестенях. Солдаты часто тонули в спирту. Требовалась особая воинская 
  сила для наведения порядка в городах. Этой силы не было. Командующий войсками 
  не справлялся, так как нужно было не только приказывать, но и следить, как дело 
  исполнялось. Участились аресты офицеров в станицах. Усиливались очаги неповиновения 
  властям. <br>
  <br>
  Агентурным путем получили известие, что большевики на Рождество выступят открыто 
  и захватят власть в Екатеринодаре. Финансовый отдел за месяц закрыл винные склады, 
  спиртное стали в Екатеринодаре выдавать в небольшом количестве. Следили за подготовкой 
  «восстания». Под Новый год пустили в продажу спиртное широко и с особым поощрением 
  для фронтовиков. Все святки перепившиеся солдаты по улицам Екатеринодара орали, 
  пели, ходили на четвереньках и просто валялись. Выступление провалилось. <br>
  <br>
  С кавказского фронта прибыл, наконец, член правительства по военным делам Успенский. 
  Он в спешке провел через Совет правительства положения о службе в кубанских 
  добровольческих отрядах и принялся сколачивать добровольческие отряды. В конце 
  святок появилось несколько отрядов. Большое значение имела популярность начальников. 
  Это были офицеры и учащаяся молодежь, но главная масса, и даже офицерство, не 
  трогались. <br>
  <br>
  В это время уже сформировалась добровольческая армия Алексеевым и Корниловым. 
  Алексеев приезжал в Екатеринодар. <br>
  <br>
  В это время появился капитан-летчик Покровский. Он обратился к правительству 
  с просьбой организовать отряд для защиты Учредительного собрания. Ему отказали. 
  Он сам организовал из молодежи отряд. С отрядом он сделал три-четыре налета 
  на большевистские отряды. Отряд стал разрастаться, а с ним и популярность Покровского. 
  <br>
  <br>
  Определилось три направления наступления большевиков на Екатеринодар: Кавказское, 
  Тихорецкое и Новороссийское. Они шли по железнодорожным магистралям. С Новороссийска 
  уже подходили к Екатеринодару. Во главе их был «военный министр Новороссийской 
  республики» прапорщик Серадже. Против них выслали два отряда Галиева и Покровского. 
  Галиев, разбив врага, был убит. Лавры победы достались Покровскому, которому 
  устроили триумфальную встречу и чин полковника. <br>
  <br>
  Длинной лентой по Красной улице Екатеринодара шли одна за другой двуколки с 
  военной добычей: пушки, пулеметы, амуниция, пленные и тяжело раненый Серадже. 
  Этот бой был началом гражданской войны и ликвидацией новороссийского направления. 
  <br>
  <br>
  В правлении все время не хватало военного руководителя. Назначили генерала Букретова. 
  Он отказался. И вот ворвалась делегация и потребовала назначить Покровского. 
  Атаман собрал правление: Быча, Успенского, Сушкова, Калабухова, Скобцова, Науменко 
  и председателя добровольческой армии генерала Эрдели. За Покровского высказались 
  Рябовол и Кулабухов. Быч с неохотой. Сушко и Скобцов просили отложить и поискать 
  еще. Науменко промолчал. Так Покровский стал во главе Кубанской армии. <br>
  <br>
  На радостях отряды Покровского перепились. Их обошли красные. Другие отряды 
  также не выполнили своей задачи. Успенский хотел уйти. Атаман попросмо его остаться. 
  Вокруг Екатеринодара кольцо смыкалось. В тюрьмах участились убийства пленных, 
  якобы пытавшихся бежать.<br>
  <br>
  Очень удручало казачество известие о смерти Каледина. Оставалась надежда на 
  помощь добровольческой армии. Ждали, рассчитывали, но вестей не было. Где затерялся 
  добровольческий отряд — никто не знал. Фронт был на подступах Екатеринодара. 
  Рада и правительство продолжали действовать и искать выход.<br>
  <br>
  Однажды Бескровный высказался, что нужно оставить надежды на Россию и соединиться 
  c Украиной. Произошла словесная свалка, и Бескровный извинился, так как он подхватил 
  только слухи. Он взял свои слова. <br>
  <br>
  Перед атаманом и правительством встал вопрос: как быть дальше?<br>
  <br>
  22 февраля войсковой атаман собрал совещание, на котором присутствовали следующие 
  20 лиц: <br>
  <br>
  - войсковой атаман полковник Филимонов; <br>
  - командующий войсками полковник Покровский; <br>
  - начальник полевого штаба полковник Науменко; <br>
  - атаман Екатеринодарского отдела полковник Косимов; <br>
  - начальник войскового штаба полковник Галушко; <br>
  - председатель Рады Рябоволов; <br>
  - член Рады Каплин; <br>
  - член правления по военным делам Успенский; <br>
  - доктор Долгополов; <br>
  - генерал от кавалерии Эрдели,<br>
  - есаул Савицкий; <br>
  - председатель правительства Быч; <br>
  - черкесского полка полковник Султан-Келич-Гирей; <br>
  - член правительства Паша-Бек; <br>
  - начальник кубанского войскового училища полковник Кузнецов; <br>
  - член Рады Бардиж; <br>
  - помощник инспектора артиллерии полковник Гальцев; <br>
  - полевой интендант полковник Галушко; <br>
  - командир гвардейского дивизиона полковник Рашпиль; <br>
  - помощник начальника полевого штаба полковник Ребдев. <br>
  <br>
  Начальник штаба и командующего войсками, по просьбе атамана обрисовали военное 
  положение, после чего атаман обрисовал общую обстановку и предложил кажому в 
  отдельности высказать свое мнение. Все высказались за то, что необходимо оставить 
  Екатеринодар. <br>
  <br>
  Кубанцы почти все были на турецком фронте. Туда также проникла большевистская 
  пропаганда, и когда произошла февральская революция, некоторые солдаты и некоторые 
  иногородние дезертировали, то есть бросили фронт. Боясь идти домой, они бродили 
  группами вдоль железных дорог, подбирали свежих дезертиров, вели пропаганду, 
  грабили население и вооружались. Позже стали бросать фронт и некоторые казаки. 
  Они со своими командами и оружием возвращались домой и под влиянием пропаганды 
  о лучшей жизни не желали подчиняться отцам и командованию и с солдатами и иногородними 
  в спор не вступали. <br>
  <br>
  В это время старое командование, атаман, областная Дума и правительство подали 
  в отставку, и были избраны новая Дума и новое правительство. <br>
  <br>
  Иногородние собирали свои комитеты. Отбирали у казаков лошадей и старались вооружиться. 
  Они активно вели пропаганду, особенно против офицеров и генералов, не брезгуя 
  ложью.<br>
  <br>
  Вокруг железнодорожных станций вооружались солдаты. Теперь у них были свои комитеты 
  с комиссарами, и они старались распространить свою власть не только на станицы, 
  но и на Екатеринодар, куда послали своих миссионеров. <br>
  <br>
  Краевая Дума выбрала правительство во главе с атаманом полковником Филимоновым 
  и председателем правительства казаком Быч. Дума состояла и из казаков, и из 
  иногородних, но председатель Думы был казак Рябовол. <br>
  <br>
  В Москве проходило Государственное совещание. От Кубани выезжали делегаты, назначенные 
  умой и правительством: Рябовол, Макаренко и комиссар от Временного правительства 
  Бардиж. Они выступали от имени Каледина. Они увидели, что Керенский не руководит, 
  митингует и нападает на Каледина. Речь Корнилова также не была авторитетна во 
  власти. Единственное, что делегаты привезли, это слово «федерация». «Только 
  федерация даст возможность развиться краю и устроить свободную жизнь по-новому 
  для всех жителей». <br>
  <br>
  Часть казаков была лояльна к государственной власти, и потому фронт еще держался. 
  Но частным образом доходили сведения, что в Государственном Совете идет разлад 
  и неполадки. <br>
  <br>
  5/18 сентября Донской Большой круг благословил свое правительство принять участие 
  по вопросу федерации для совещания в Екатеринодаре. Совещание-конференция, организованная 
  с трудом, собралась во время «мятежа Корнилова» и пришла к выводу, что это дело 
  провокации борющихся за власть сторон. Они требовали тщательного разбора этого 
  дела с допуском представителей от казачества. Иначе казаки сделают свои выводы. 
  Что касается федерации «Юго-востока», то выделили делегатов на следующую конференцию, 
  которая должна была проходить с принятием устава и федерального договора в столице 
  Терского войска — Владикавказе. <br>
  <br>
  Все левые подняли вой против федерации, дескать, вы готовы дружить и объединяться 
  с калмыками, черкесами, но не с нами, русскими. <br>
  <br>
  К концу сентября авторитет центральной власти совершенно упал. Комитет иногородней 
  части правительства, которые ушли с Краевой Рады и правительства, ночью с 3-го 
  на 4-е июня превратился теперь в особый агентурный пункт против войскового правительства. 
  Узловые железнодорожные станции: Армавир, Тихорецкая, Кавказская — забивались 
  «законными» и «незаконными» дезертирами. Они под разными предлогами приставали 
  к местным запасным частям, где их прикармливали и увеличивали мощность противников 
  казачества. <br>
  <br>
  В Екатеринодаре собралась войсковая Рада, чтобы разрешить вопрос о самоуправлении 
  и управлении. Иван Макаренко принес проект об управлении. Всю ночь разбирали 
  и изменяли и к утру отправили проект на доработку.<br>
