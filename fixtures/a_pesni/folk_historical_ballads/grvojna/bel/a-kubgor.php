       <span class="header"><strong>ПРИКАЗ ОБ ИЗМЕННИКАХ </strong><br></span>
  <br>
  Таганрог. (Срочно). Генералу Лукомскому, Донскому атаману, Кубанскому атаману, 
  Терскому Атаману, Командарму Кавказской, Командарму Добровольческой, Командарму 
  Донской, Командующему войсками Северного Кавказа, Главноначальствующему Новороссийской 
  области, Военному Губернатору Черноморской губернии. <br>
  <br>
  В июле текущего года между Кубанью и Меджилисом горских народов заключен договор, 
  в основу которого положена измена России и передача Кубанских казачьих войск 
  Северного Кавказа в распоряжение Меджилиса, чем обрекается на гибель Терское 
  войско. <br>
  <br>
  Договор подписан Бычем, Савицким, Калабуховым и Намитоковым, с одной стороны, 
  и Черчоевым, Гайдаровым, Хадзараговым и Бамматовым, с другой. <br>
  <br>
  Приказываю при появлении этих лиц на территории вооруженных сил юга России немедленно 
  предать их военно-полевому суду за измену. <br>
  <br>
  Таганрог, 25 октября 1919 года, № 016727 <br>
  Деникин <br>
  Романовский <br>
