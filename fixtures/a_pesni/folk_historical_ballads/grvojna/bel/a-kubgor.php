	<span class="header"><strong>ДОГОВОР 
        ДРУЖБЫ МЕЖДУ ПРАВИТЕЛЬСТВАМИ КУБАНИ И РЕСПУБЛИКИ СОЮЗА ГОРЦЕВ КАВКАЗА 
        </strong><br></span>
  <br>
  Кубанская делегация, с одной стороны, и делегация Союза Горских Народов, с другой, 
  исходя из твердого убеждения, что политическое, культурное и экономическое преуспевание 
  Кавказа и пользование благами мира возможно лишь при упрочении дружественных 
  отношений между кавказскими государствами и при взаимной их поддержке друг другом 
  признали необходимым заключить между собой следующий договор: <br>
  <br>
  1. Правительство Кубани и правительство республики Горских Народов Кавказа настоящим 
  торжественным актом взаимно признают государственный суверенитет и полную политическую 
  независимость Кубани и Союза Горских Народов Кавказа. <br>
  <br>
  2. Установление территориальных границ между Кубанью и республикой Союза Горских 
  Народов имеет быть предметом особых последующих дружественных соглашений между 
  договаривающимися сторонами. <br>
  <br>
  3. Договаривающиеся стороны обязуются не предпринимать ни самостоятельно, ни 
  в форме соучастия с кем бы то ни было никаких мер, клонящихся к уничтожению 
  или умалению суверенных прав Кубани и республики Союза Горских Народов Кавказа. 
  <br>
  <br>
  4. Войсковые части из договаривающихся сторон могут переходить на территории 
  другой стороны не иначе, как по просьбе или с согласия правительства этой стороны. 
  Войска одной стороны, находящиеся на территории другой, поступают в подчинение 
  этой последней. <br>
  <br>
  5. Признавая желаемым самое тесное экономическое сближение между договаривающимися 
  сторонами, правительство Кубани и правительство Союза Горских Народов обязуются 
  урегулировать путем особых дополнительных договоров вопросы: <br>
  <br>
  А — товарообмена и таможенный, Б — транспорта (железных, водных и грунтовых 
  путей), В — почты и телеграфа, Г — денежного обращения и др. <br>
  <br>
  Настоящий договор подлежит утверждению в порядке, установленном конституциями 
  договаривающихся сторон. <br>
  <br>
  Договор сей составлен в двух экземплярах в городе Париже в июле 1919 года. <br>
  <br>
  Подлинный подписали: <br>
  <br>
  со стороны Кубани <br>
  Л. Быч — председатель делегации,<br>
  В. Савицкий — член делегации, <br>
  В. Кулабухов — член делегации, <br>
  А. Намитоков — член делегации; <br>
  <br>
  со стороны Союза Горских Народов Кавказа <br>
  А. Черноев — председатель делегации, <br>
  И. Гайдаров — член делегации, <br>
  Х. Хадзарагов — член делегации, <br>
  Г. Баммат — министр иностранных дел. <br>
  <br>
  <em>Закаспийский бюллетень. 1919. 10.11. №3.</em><br>
  <br>
