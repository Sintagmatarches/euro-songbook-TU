  выделить из Иркутского казачьего полка одну сотню и отправить на Оренбургский 
  фронт не позднее 15 Апреля нового ст[иля]. <br>
  <br>
  На полку, как единственной казачьей части в гарнизоне г. Иркутска, лежит охранение 
  государственного порядка и обеспечение тыла. На днях проведена дополнительная 
  мобилизация для пополнения полка, и ввиду необходимости сформироваться, обмундироваться, 
  вооружить и дать хотя бы краткосрочную подготовку, сотня, предназначенная к 
  отправке на фронт, задерживается до 15 Апреля. <br>
  <br>
  Препятствий к отправке сотни со стороны высшего командования в округе не имеется 
  и Войсковое Правление с чувством глубокой радости извещает Вас, г[осподин] Атаман, 
  что горячее желание маленького войска помочь старшим братьям в минуту тяжелой 
