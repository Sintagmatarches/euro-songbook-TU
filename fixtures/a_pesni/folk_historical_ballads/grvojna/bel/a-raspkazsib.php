        <span class="header"><strong>РЕКОНСТРУКЦИЯ БОЕВОГО РАСПИСАНИЯ КАЗАЧЬИХ ВОЙСК УРАЛА, СИБИРИ 
        И ДАЛЬНЕГО ВОСТОКА ПО СОСТОЯНИЮ К 25 ОКТЯБРЯ 1919 г. <br>
        </strong></span><br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 302-304.<br>
  <br>
  <br>
  К сожалению, боевых расписаний Восточного фронта белых за вторую половину 1919 
  — начало 1920 гг. в фондах Российского государственного военного архива до сих 
  пор обнаружить не удалось. Сравнительно хорошо сохранились аналогичные боевые 
  расписания, составленные штабом Восточного фронта РККА на основе разведданных, 
  однако эти расписания изобилуют самыми серьезными искажениями, поэтому единственно 
  возможным способом уточнения данных о казачьих частях востока России является 
  реконструкция боевого расписания на основе всего комплекса документов и материалов, 
  имеющихся в нашем распоряжении. Именно такая реконструкция боевого расписания 
  казачьих войск Урала, Сибири и Дальнего Востока по состоянию к 25 октября 1919 
  г. и предлагается вниманию читателей. Реконструкция отражает организационную 
  структуру казачьих формирований востока России в канун Сибирского Ледяного похода. 
  Наибольшие трудности вызвала реконструкция расписания Отдельной Уральской армии. 
  Автор сознает возможность отдельных неточностей и неполноты сведений при реконструкции 
  расписания и с благодарностью примет все уточнения и дополнения. </em><br>
