	<br><span class="text"><strong>Сергей 
  Мальков, сотник Великого войска Донского, г. Пенза</strong><br>
  <br>
  <span class="header"><strong>ИЗОБИЛЬНЕНСКОЕ ДЕЛО (ВОССТАНИЕ ИЛЕКСКИХ СТАНИЦ ОРЕНБУРГСКОГО КАЗАЧЕСТВА)</strong><br></span>
  <br>
        <em>Газета "Казачий взгляд", №5 (101), май 2006</em>.<br>
  <br>
  <br>
  Февраль 1918 г. Оренбургские степи. В январе пал Оренбург, большевистские войска 
  упорно теснили немногочисленные казачьи отряды атамана А. И. Дутова на восток, 
  к Тургаю. <br>
  <br>
  В оставленные станицы пришла "власть рабочих и крестьян". Начались кровавые 
  расправы, грабежи, разбой. Угоняли скот, вывозили хлеб. Все станицы, независимо 
  от участия в борьбе против большевиков, обложили налогами и денежными контрибуциями. 
  Несколько станиц вовсе сожгли дотла. <br>
  <br>
  Большевики всех казаков считали врагами и потому не церемонились. Много офицеров, 
  чиновников, казаков и даже казачек было расстреляно; ещё больше арестовано и 
  посажено в тюрьму. Такие действия быстро отрезвили тех казаков, кто ещё верил 
  в "нейтралитет" (особенно - фронтовики). Из линейных станиц, расположенных по 
  реке Илеку (приток Урала), первый сигнал к восстанию подала станица Изобильная. 
  В 20-х числах февраля в неё прибыла из Оренбурга группа красных матросов (20 
  человек) для взимания налогов с казачьего населения, что превратилось в подлинный 
  грабёж. <br>
  <br>
  Казаки побили весь отряд, тела спустили под лёд Илека. Прекрасно понимая, что 
  последует жестокая кара, по окрестным станицам были посланы гонцы с призывом 
  о помощи. Станичные отряды стали стягиваться в район станиц Изобильной и Буранной, 
  командиром повстанцев избран есаул Сукин, его заместителем - подъесаул Донецков. 
  <br>
  <br>
  На подавление восстания из Оренбурга и Соль-Илецка выступил отряд "красной гвардии" 
  силой в 800 штыков при пушках и пулемётах. Командовал карателями известный оренбургский 
  комиссар Самуил Моисеевич Цвиллинг. <br>
  <br>
  Первая стычка произошла под станицей Мертвецовской (ныне село Трудовое), казачья 
  лава была отбита и большевики заняли Мертвецовскую, а следом и Ветлянскую станицы. 
  <br>
  <br>
  Казаки пошли на военную хитрость: сделали засаду и создали видимость отступления. 
  Когда цепи красных бойцов втянулись в станицу Изобильную, неожиданно раздался 
  выстрел бомбомёта, следом ружейный залп. Обходя с флангов, вынеслась казачья 
  конница. Орудийные и пулемётные расчёты пали под ударами шашек и пик казаков, 
  самого Цвиллинга срубил хорунжий Орлов - казацкий клинок развалил голову сыну 
  парикмахера. Лишь небольшой группе большевиков удалось спастись бегством. <br>
  <br>
  Трофеями казаков были 4 пушки, 12 пулемётов, 700 винтовок, патроны, снаряды. 
  Священный чин на поле боя отслужил благодарственный молебен. Эти события получили 
  название "Изобильненского дела", и боевой дух оренбургского казачества вновь 
  поднял на борьбу вольных сынов Урала и Илека. <br>
  <br>
  Примерно в это же время (начало апреля 1918 г.) укрывавшиеся по станицам и хуторам 
  офицеры подняли восстание в верхних станицах 1-го округа (по рекам Уралу и Самаре, 
  выше Оренбурга). Командовал восставшими войсковой старшина Лукин - он организовал 
  поход на Оренбург. Вначале дела шли хорошо, комиссары из станиц были изгнаны, 
  Советы уничтожены. 4-го апреля после жаркого боя был взят повстанцами Оренбург, 
  но плохо вооружённые и организованные станичные дружины не смогли удержать город 
  в своих руках и отступили. <br>
  <br>
  Стоит отметить наиболее стойкие в борьбе с большевизмом станицы: Карагайская, 
  Петропавловская, Краснинская, Кассельская, Остроленская, а также те, которые 
  большевикам помогали: Арсинская, Краснохолмская и Верхне-Уральская. Как отмечал 
  Иван Акулинин (помощник Войскового Атамана, командир казачьих корпусов), "очень 
  часто большие и богатые станицы если и не держали открыто сторону большевиков, 
  то всеми способами уклонялись от борьбы с ними, и наоборот, - небольшие и бедные 
  станицы проявляли к большевикам крайнюю ненависть и при первой возможности готовы 
  были драться с ними". <br>
  <br>
  События на Оренбуржье не менее драматичны, чем на Тихом Дону. К чести оренбуржцев, 
  выдачи офицеров на расправу большевикам здесь не было. 15-й Оренбургский казачий 
  полк, бывший на Юго-Западном фронте, чтобы не отдавать большевикам оружия, прибыл 
  в Войско походным порядком, пройдя через всю южную и юго-восточную Россию. <br>
  <br>
  Партизанское движение оренбургского казачества до конца не изучено, но известны 
  такие вожди казаков-партизан, как войсковой старшина Мамаев, подъесаулы Бородин, 
  Михайлов, Енборисов. Последний - Енборисов, - следуя примеру Тараса Бульбы, 
  не пощадил своего сына-офицера, ушедшего к большевикам "служить трудовому народу"! 
  Впрочем, и сами большевики "отблагодарили" таких вождей "красного казачества", 
  как, например, командарма Николая Каширина (бывший подъесаул) в 1937 году. Девятью 
  граммами свинца... <br>
