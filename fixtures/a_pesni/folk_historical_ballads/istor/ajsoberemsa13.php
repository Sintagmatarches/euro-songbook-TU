АЙ, СОБЕРЁМСЯ, РЕБЯТУШКИ</h1>
  <br>
  Ай, соберёмся, ребятушки, е-ай, <br>
  <div class='refren'>Мы, каза... ей, мы, казачушки, <br>
    Ай, во единый круг, </div>
  Ай, во единый круг; вот и запоём, е-ай, <br>
  <div class='refren'>Мы, каза... ей, мы, казачушки, <br>
  Ай, песню старую, </div>
  Ай, песню старую, мы, ребятушки, е-ай, <br>
  <div class='refren'>Старода... ей, стародавнюю, <br>
 Ай, небывалую. </div>
  Ай, что певали ребятушки, е-ай, <br>
  <div class='refren'>На синё... ей, на синём море, <br>
    Ай, на Каспицковым, </div>
  Ай, на Каспицковым да на морюшке, е-ай, <br>
  <div class='refren'>На чермён, ей, на кораблике, <br>
  Ай, на Сок<strong>о</strong>л-корабле, </div>
  Ай, да не песни мы пели, горе мы... е-ай, <br>
 <div class='refren'>Горе мы... ей, горе мыкали, <br>
  Ай, горе горькое, </div>
  Ай, спотешали ребятушки, е-ай, <br>
  <div class='refren'>Да на мо... ей, да на морюшке, <br>
   Ай, атаманушку: </div>
  „Ай, атаман ты наш, атаманушка, е-ай, <br>
 <div class='refren'>Родный ба... ей, родный батюшка, <br>
  Ай, наш Степанушка! </div>
  Ай, наш Степанушка, ты ли был-побывал, е-ай, <br>
<div class='refren'>На синё... ей на синём море, <br>
  Ай, на Каспицковым, </div>
  Ай, на Каспицковым, ну видал ли там, е-ай, <br>
  <div class='refren'>Ну, такой ей, страсти-у... <br>
  Ай, страсти-ужасти? </div>
  Ай, сподымалася со синя моря, е-ай, <br>
  <div class='refren'>Вот невзго... ей, вот невзгодушка, <br>
  Ай, она бурная, </div>
  Ай, буря бурная, непогожая, е-ай, <br>
<div class='refren'>Понесла ей, наш Сок<strong>о</strong>л-корабль, <br>
  Ай, на круту скалу, </div>
  Ай, на круту скалу, на трухменую, е-ай, <br>
 <div class='refren'>Опроки... ей, опрокидывала, <br>
    Ай, к верху донышком&quot;. </div>
