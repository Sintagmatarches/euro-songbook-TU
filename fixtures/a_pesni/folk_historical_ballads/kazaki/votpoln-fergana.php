  <strong>Завоевание Средней Азии 1850-80-е гг.</strong><br>
  <br>
  К середине XIX в. на территории Туркестана существовало три государственных 
  образования без четких границ: Бухарский эмират (3 млн. человек), Кокандское 
  ханство (1,5 млн.) и Хивинское ханство (500 тыс.). Населяли их люди, которых в советских переписях населения стали учитывать как узбеков, таджиков, туркмен, казахов, киргизов и каракалпаков. Серьезной военной силы 
  эти образования из себя не представляли, к тому же периодически между ними шли 
  междуусобицы, вспыхивали мятежи и восстания. Тем не менее, некоторые 
  города оказали упорное сопротивление русским: уличные бои в Ташкенте шли более 
  двух дней. В конечном итоге Кокандское ханство было упразднено (1876), Бухара 
  и Хива потеряли часть территорий, в том числе доступ к водоемам, и признали 
  полную вассальную зависимость от России (1868 и 1873). На жизненный уклад местного 
  населения русское завоевание не повлияло. <br>
  <br>
  Контроля над районом российские власти добивалась прежде всего, вероятно, в интересах торговли: российская 
