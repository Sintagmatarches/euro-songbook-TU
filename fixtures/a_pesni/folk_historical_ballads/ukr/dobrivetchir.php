<br>
«Чи то ж би я розуму не мала,<br>
Щоб я тобi правдоньку сказала?»<br>
<br>
«Добрий вечiр, дiвчино, чия ти?<br>
Чи вийдеш ти на вулицю гуляти?»<br>
<br>
«Не питайся, козаченьку, чия я,<br>
Як вийдеш ти на вулицю, вийду й я!»<br>
<br>
«Дiвчинонько-серденько, куди йдеш?<br>
Скажи менi правдоньку, де живеш!»<br>
<br>
«Отам моя хатонька край води,<br>
З високого дерева, з лободи.<br>
<br>
Я ж до хати сiнечки приплету<br>
З високого дерева, з черету!»<br>
<br>
«Тепер же я догадався, чия ти!<br>
Пусти ж мене, серденько, до хати!»<br>
<br>
«Шкода теє, голубе, затiвать,<br>
Бо ти будеш нiченьку ночувать!»<br>
<br>
«Та я ж тебе, серденько, не зведу,<br>
Пожартую трiшечки та й пiду!»<br>
<br>
Жартувала дiвчина до зорi,<br>
Поки стало виднесенько надворi.<br>
<br>
«Ой, уставай, козаченьку, та тiкай,<br>
Та на мене славоньки не пускай!»<br>
