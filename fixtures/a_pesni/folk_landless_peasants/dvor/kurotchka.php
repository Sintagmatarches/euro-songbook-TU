        Гусонька — гаи, гаи, <br>
        Барашек — шадры, бадры, <br>
        Индюшечка — шулды, булды, <br>
        Уточка — с носка плеска, <br>
        Курочка по семечку клюет, <br>
        Клюет, клюет, поклёвывает. <br>
