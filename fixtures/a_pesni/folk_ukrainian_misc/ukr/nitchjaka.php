 <br> 
 Песня на основе стихотворения &quot;Виклик&quot; малороссийского драматурга, поэта, переводчика, одного из основателей украинского театра <strong>Михаила Старицкого</strong> (Михайло Петрович Старицький, 1840-1904). Стихотворение написано в 1870 г. Как песня утвердилось в музыкальной обработке <strong>Николая Лысенко</strong> (Микола Віталійович Лисенко, 1842-1912).<br>
 <br>
 В распространенной песенной версии первая рифма (<em>зоряна -  зморена</em>) утрачена. Во-первых, так нередко происходит в песнях - ср. например, стихийное  &quot;Как родная меня <strong>мать</strong> / Провожала, / 
Как тут вся моя <strong>родня</strong> /
Набежала&quot; с авторским &quot;Как родная мать <strong>меня</strong>...&quot; Демьяна Бедного. Во-вторых, рифма выбивалась даже из авторского стихотворения: не считая &quot;зоряна - зморена&quot; автор избегает точных рифм (в которых совпадают гласная и согласная) в 1-й и 3-й строках строф, и строго соблюдает во 2-й и 4-й.<br> 
 <br> 
 Песня использована в фильме &quot;В бой идут одни старики&quot; (1974).<br>
