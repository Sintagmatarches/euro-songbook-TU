Ой у поле криниченька, <br>
Там холодна водиченька. <br>
<br>
Ой там Роман воли пасе, <br>
Катерина воду несе. <br>
<br>
Ой став Роман жартувати, <br>
З вiдер воду виливати. <br>
<br>
Ой Романе, Романочку, <br>
Не лий воду на сорочку. <br>
<br>
Бо не рiдну неньку маю, <br>
Буде бити, добре знаю. <br>
<br>
Буде бити, ще й лаяти, <br>
Ще й Романом докоряти. <br>
<br>
Де ж ти, доню, барилася, <br>
Вже й вечеря зварилася. <br>
<br>
Налетiли гуси з броду, <br>
Сколотили в ставi воду. <br>
<br>
А я стала, постояла,<br>
Доки вода устоялась. <br>
<br>
Брешеш, доню, не стояла, <br>
Ти з Романом розмовляла. <br>
<br>
Щиру правду признаюся, <br>
Що з Романом кохаюся. <br>
<br>
Роман чорний, я бiлява, <br>
Поберемось - буде пара. <br>
<br>
Буде пара, ще й парочка,<br>
Ще й маленька Наталочка.<br>
