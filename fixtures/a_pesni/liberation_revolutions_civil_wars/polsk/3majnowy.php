	<span class="header">
  <strong>Treci Maj Nowy <br>
  (Третий Май новый)</strong><br></span>
  <br>
  Słowa: Konstanty Gaszyński<br>
  <br>
  Pamiętasz bracie kochany, <br>
  Gdyśmy zawsze w dni wiosnowe, <br>
  Wychodzili na Bieiany <br>
  Obchodzić święto Majowe -<br>
  <br>
  Nucąc wraz: Boże daj, <br>
  Ву nam lepszy nadszedł Maj.<br>
  <br>
  Ci, którzy nam więzy dali, <br>
  Znosząc Majową ustawę, <br>
  Jeszcze, srodzy, zabraniali <br>
  Święcić drogą Ojców sławę.<br>
  <br>
  Smutno żyć, smutno żyć, <br>
  Gdy się trzeba z sercem kryć.<br>
  <br>
  Kto wspominał Polskę kochaną, <br>
  Jęczyć musiał długą chwilę — <br>
  Weselić się nam kazano <br>
  Na matki naszej mogile.<br>
  <br>
  Kraju nasz, kraiu nasz, <br>
  Taką tо ty wolność masz?<br>
  <br>
  Wszak nasza czapka czerwona, <br>
  Wąs, czamara z pasem drogim, <br>
  Naszego rodu znamiona <br>
  Znikły przed ukazem srogim. <br>
  <br>
  Wrogu stój, wrogu stój,<br>
  Chcesz-że wydrzeć nam i strój? <br>
  <br>
  Gdу tak dręczyli tyrany, <br>
  Pamiętasz kochany bracie? <br>
  Chodziliśmy na Bielany <br>
  Płakać ро wolności stracie —<br>
  <br>
  Nucąc wraz: Boże daj, <br>
  Ву nam lepszy nadszedł Maj! <br>
  <br>
  Lecz z nas męstwo nie upada. <br>
  Każdy ramię wzniósł gotowe, <br>
  I jedna noc Listopada <br>
  Wróciła nam dni Majowe.<br>
  <br>
  Przyszedł cas, przyszedł cas,<br>
  Który z kajdan rozkuł nas.<br>
  <br>
  Nasza młodzież nie zwalczona -<br>
  Zadrżeli przed nią wrogowie, <br>
  I znowu czapka czerwona <br>
  Zabłysła na wolnych głowie.<br>
  <br>
  Polski wąs, polski strój, <br>
  Głos-że wrogu zakaz twój.<br>
  <br>
  A więc bracie mój kochany, <br>
  Gdy nadejdą dni wiosnowe, <br>
  Znów pójdziemy na Bielany <br>
  Obchodzić święto majowe -<br>
  <br>
  Nucąc wraz: Boże daj, <br>
  Ву kwitł wiecznie taki Maj!<br>
  <br>
  <em>Jeszcze Polska nie zginęła! Pieśni patryotyczne i narodowe. Zebrał 
  Francisek Barański. New York, N.Y., The Polish Book Importing Co., Inc., 1944. Nr. 29 b. </em><br>
 <br>
  <br>
  Песня на стихи поэта эпохи Ноябрьского восстания 1830-1831 гг. Константина Гашиньского. 
  Это одна из песен памяти Конституции Третьего мая 1791 г., сложенных на мотив 
  популярной мелодии, предположительно авторства Фредерика Шопена. См. родственные 
  песни: <a href="3maj.php">&quot;Witaj, majowa jutrzenko&quot;</a> (или &quot;Treci 
  maj&quot; Райнольда Суходольского, написана в апреле 1831 г., там же ноты); бытовавшую 
  накануне восстания конспиративную <a href="nienawidze.htm">&quot;Nienawidzę 
  was próżniaki&quot;</a> Станислава Старжевского (со второй половины XIX века 
  песенка Старжевского исполняется на иной мотив), а также <a href="biednykraj.php">&quot;Biedny 
  kraj&quot;</a>.<br>
