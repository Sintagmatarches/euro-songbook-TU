        <br>
        Zog nit keynmol az du gayst dem letzten veg, <br>
        Ven himlen blayene farshteln bloye teg; <br>
        Vayl kumen vet noch undzer oysgebenkte shuh, <br>
        Es vet a poyk tun undzer trot - mir zaynen do! <br>
        <br>
        Fun grinem palmenland biz land fun vaysen shney, <br>
        Mir kumen un mit undzer payn, mit undzer vey; <br>
        Un voo gefalen iz a shpritz fun undzer blut, <br>
        Shpritzen vet dort undzer gvure, undzer mut. <br>
        <br>
        Es vet di morgenzun bagilden undz dem haynt, <br>
        Un der nechten vet farshvinden mitn faynt; <br>
        Nor oyb farzamen vet di zun in dem ka-yor, <br>
        Vi a parol zol geyn dos leed fun door tzu door. <br>
        <br>
        Geshriben iz dos leed mit blut und nit mit bly, <br>
        S'iz nit keyn leedl fun a foygel oyf der fry; <br>
        Dos hut a folk tzvishen falendike vent, <br>
        Dos leed gezungen mit naganes in di hent. <br>
        <br>
