<br>
Когда мне было лет пятнадцать,<br>
  Я не знала ничего.<br>
  Когда исполнилось семнадцать,<br>
  Полюбила одного.<br>
<br>
Тайно я его любила,<br>
  Тайно он меня любил,<br>
  Наше счастье не свершилось,<br>
  Враг навеки разлучил.<br>
<br>
Его мать не пожелала<br>
  Наше счастье совершить:<br>
  Сыну строго наказала<br>
  Меня, бедную, забыть.<br>
<br>
Сын с такой большой печали<br>
  Слег в постель и захворал.<br>
  Перед самой его смертью<br>
  Он заплакал и сказал:<br>
<br>
- Позволь, маменька, жениться,<br>
  Позволь взять кого люблю!<br>
  Соверши ты наше счастье,<br>
  Я, быть может, оживу!<br>
<br>
Мать позволила, но поздно,<br>
  Сыну смерть была близка:<br>
  Только вспомнил ее имя<br>
  И навек закрыл глаза.<br>
<br>
Была тихая погода,<br>
  Ярко светила луна,<br>
  А между частыми крестами<br>
  Пробиралася она.<br>
<br>
Она нашла его могилу,<br>
  Тихо-тихо подошла,<br>
  И на могиле написала,<br>
  И на могиле померла:<br>
<br>
«Люди добрые, поверьте,<br>
  Как любила я его,<br>
  И на части растерзали<br>
  Мое сердце и его!»<br>
