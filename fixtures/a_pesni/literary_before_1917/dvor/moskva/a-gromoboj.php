  <br>
  Сопоставление фрагментов, использованных в фольклоре, и полного авторского текста 
  показывает, что из поэтического состава оригинала не востребованными традицией 
  остались строки, живописующие мрачный романтический пейзаж и содержащие диалог 
  с дьяволом, а также его поучения. Значительно сокращены печальные размышления 
  героя; что же касается речей Асмодея (в диалоге с героем), то они преобразованы 
  в его монолог. Все это соответствует поэтике фольклорной баллады, вообще чуждой 
  обстоятельных описаний (тем более — описаний душевных состояний) и избегающей 
  пересказа пространных бесед. Кроме того, несколько изменена последовательность 
  строф (см. их нумерацию в вышеприведенном тексте) — устная традиция перекомпоновывает 
  используемые фрагменты сообразно логике повествования, упростившегося за счет 
  указанных сокращений.<br>
  <br>
  Поскольку данная композиция в фольклорной версии нигде не нарушается, можно 
  предположить, что все известные нам варианты восходят к одной лубочной переработке 
  исходного стихотворения, имеющей в своей основе монтаж фрагментов, последовательность 
  которых отражена в пятой колонке Таблицы II. Этому могло бы противоречить наличие 
  в трех случаях (они помечены литерой а) альтернативных использований разных 
  строк оригинала в одной и той же позиции устных редакций, когда востребованные 
  фольклором строки оказываются вариантными по отношению к другим строкам первотекста:<br>
