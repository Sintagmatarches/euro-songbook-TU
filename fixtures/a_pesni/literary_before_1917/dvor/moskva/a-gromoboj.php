        </strong><br>
<span class="header"><strong>ФОЛЬКЛОРНЫЕ ПЕРЕРАБОТКИ РУССКОЙ ПОЭЗИИ XIX ВЕКА: БАЛЛАДА 
  О ГРОМОБОЕ</strong><br></span> 
  <br>
        <em>И время и место. Историко-филологический сборник к шестидесятилетию 
        Александра Львовича Осповата. М.: Новое издательство, 2008. С. 574-593. 
        Приводится по сайту <a href="http://www.ruthenia.ru/folklore/neckludov30.htm" target="_blank">&quot;Фольклор 
        и постфольклор&quot;</a></em><br>
  <br>
  <br>
  <strong>1.</strong><br>
  <br>
  Городская песня — один из основных жанров фольклора XX в. Как и все фольклорные 
  произведения, данные тексты не являются санкционированными нормативной господствующей 
  культурой и в известном смысле противостоят ей, нарушая ее каноны, а иногда 
  пародируя и переиначивая ее (впрочем, переиначиваемые тексты могут быть и навязанным 
  сверху, и идеологически нейтральными или принадлежащими самой «уличной» традиции). 
  Они распространяются устным путем и через рукописные песенники, а также через 
  магнитофонные записи, позволяющие тиражировать и передавать — вне официально 
  контролируемых каналов — не только текст песни, но и ее исполнение. Очень рано 
  городская песня попадает в деревню и закрепляется там в репертуаре сельских 
  исполнителей (особенно исполнительниц), вытесняя традиционную народную лирику.<br>
  <br>
  Эти песни (их часто называют <em>романсами</em> и <em>балладами</em>) имеют 
  строфическую структуру (обычно по 4 строки в строфе), с перекрестной, смежной, 
  опоясывающей, но неустойчивой и неточной рифмой. Поются они в сопровождении 
  гитары, аккордеона, фортепьяно (реже) или без аккомпанемента, соло и хором (или 
  с хоровым подхватом, с хоровым припевом). При их перемещении в деревню исполнительская 
  манера модифицируется: напевы становятся протяжнее, появляются повторы строк, 
  не встречающиеся в более кратких городских вариантах.<br>
  <br>
  Жанровый предшественник песен этого типа — городской («мещанский») романс, в 
  основе которого лежит фольклоризуемая литературная песня XIX в.[1] Ее далекие 
  истоки, как и вообще истоки непрофессионального вокального музицированья в России, 
  восходят к рубежу XVI — XVII в., когда среди духовных псальмов (Ростовского, 
  Полоцкого, Скорины и др.) появляются <em>псальмы</em> светские — трехголосные 
  песни, анонимные и авторские, без музыкального сопровождения и без четкой строфической 
  структуры.<br>
  <br>
  Начиная с Петровской эпохи, они вытесняются светскими <em>кантами</em> — также 
  авторскими (Кантемира, Прокоповича, Ломоносова, Сумарокова, Тредиаковского и 
  мн. др.) и анонимными, сперва трех-, затем двух- и одноголосными, уже имеющими 
  четкое членение на строфы и отчетливый ритм, но еще исполнявшимися без музыкального 
  сопровождения. Сначала возникают песни военно-исторической тематики (панегирические 
  и патриотические <em>виваты</em> [Позднеев 1996, с. 120-180]), потом тематика 
  их расширяется (псальмические, приветственные, заздравные, застольные, любовные, 
  пасторальные, шуточные, пародийные); центральное место среди «увеселительных» 
  <em>кантов</em> занимают «песни любовные городские» — их принадлежность к урбанистической 
  культуре осознается и подчеркивается самой традицией [Гудошников 1990, с. 13]. 
  В отличии от литературных <em>виршей</em>, предназначенных для чтения, <em>канты</em> 
  пишутся специально для вокального исполнения [Позднеев 1996, с. 124]. Они широко 
  распространяются (в том числе через рукописные нотные песенники) практически 
  по всем слоям тогдашнего российского общества — вплоть до низовой городской 
  и даже деревенской среды [Гусев 1988, с. 11-13].<br>
  <br>
  Во второй половине XVIII в. возникает так называемая <em>российская песня</em> 
  — бытовой романс для сольного одноголосного исполнения в сопровождении клавесина, 
  фортепиано, гуслей или появившейся тогда же гитары. Переходной формой от <em>канта</em> 
  к <em>российской песне</em>, точнее к <em>романсу</em>, исполняемому дуэтом, 
  считается сборник Г.Н. Теплова «Между делом безделье» (1759), в котором уже 
  есть аккомпанемент на скрипке или флейте, причем в это инструментальное сопровождении 
  «уходит» третий голос <em>канта</em>. Примечательно, что если <em>канты</em>, 
  а тем более светские <em>псальмы</em>, естественно, звучащие только по-русски, 
  совершенно не нуждались в какой-либо специальной манифестации своей «русскости», 
  то <em>российская песня</em> называется так, потому что противопоставляется 
  французским «романсам». Среди ее тематических и жанровых разновидностей (идиллическая 
  «пастушеская», веселая застольная, элегическая, дидактическая, философская) 
  важное место занимают переложения народной песни и подражания ей, а при создании 
  стихотворений «на голос», т.е. на известную мелодию, большое значение имеет 
  ориентация на фольклорную лирику [Гусев 1988, с. 13-19]. Обратим внимание, что 
  происходит это в предромантическую эпоху, когда осознается ценность народной 
  традиции, «высокая» культура поворачивается к ней лицом, а писатели (Гердер, 
  Бюргер, Гете, Лессинг и др.) записывают ее и используют в своем творчестве.<br>
  <br>
  Тем более это относится к новому типу бытового романса — к появившейсяв конце 
  XVIII — начале XIX в. <em>русской песне</em> (она же <em>деревенская</em> или 
  <em>сельская</em>). Она предполагает уже сознательное (романтическое и патриотическое) 
  обращение к фольклору, а в музыкальном отношении близка к «гитарным» обработкам 
  народных песен. «Классики» жанра (А.Ф. Мерзляков, А.А. Дельвиг, Н.Г. Цыганов, 
  А.В. Кольцов и др.) прямо имитируют строй устной поэзии, используя ее образную 
  систему, стилистические формы, поэтическую лексику. Некоторое количество текстов, 
  утрачивая имена авторов, фольклоризуется, становясь полноценными народными песнями 
  (как, скажем, произведения Н.Г. Цыганова); подобный процесс облегчается тем 
  обстоятельством, что основными поставщиками <em>русской песни</em> были поэты 
  отнюдь не первого ряда, фамилии которых мало что говорили следующим поколениям 
  читателей, слушателей и исполнителей этих песен [Гусев 1988, с. 19-26; Гудошников 
  1990, с. 5-6; Селиванов 1999, с. 6]. При этом есть случаи, когда «классическая» 
  народная песня обрабатывается литератором (скажем, «Степь Моздокская» И.З. Суриковым, 
  «Ванька-ключник» В.В. Крестовским), после чего обработка возвращается в фольклор, 
  но уже в городской (а оттуда — обратно в деревенский) и, естественно, без имени 
  автора. Таким образом, литературно-фольклорные отношения здесь оказываются двусторонними: 
  светские <em>канты</em>, распространяющиеся в низовой фольклорной среде; <em>российская 
  песня</em>, подражающая народной лирике; <em>русская песня</em>, с одной стороны, 
  имитирующая строй устной поэзии, а с другой, активно фольклоризуемая.<br>
  <br>
  Примерно с 60-х годов XIX в. романсная традиция раздваивается. Одна ее часть 
  («камерный романс») становится уделом профессиональных музыкантов, все более 
  «усложняющегося мастерства», другая (мещанский, или городской романс) уходит 
  в низовую культуру, взаимодействуя с «бульварной поэзией» (вроде стихотворных 
  надписей на лубочных картинках [Гудошников 1990, с. 9-10]) и с «выходами на 
  весьма популярную, но малоуважаемую демократическую эстраду» [Петровский 1997, 
  с. 7-10]. Именно на базе мещанского романса складывается городская фольклорная 
  песня XX в., в известных нам формах, вероятно, возникшая в 1910-х — 1920-х годах, 
  причем эпицентрами ее распространения скорее всего были южные города, в первую 
  очередь, Одесса [Неклюдов 2003, с. 71-86].<br>
  <br>
  Впрочем, городская песня XX в. опирается и на другие источники — как литературные 
  [Козьмин 2005, с. 40-42], так и эстрадные. С эстрадными истоками связана и куплетная 
  форма некоторых текстов, не имеющих устойчивого сквозного сюжета, причем набор 
  и последовательность куплетов могут варьироваться в весьма широких пределах. 
  Двустороннее взаимодействие уличной песни с «низовой» (ресторанной, садово-парковой) 
  эстрадой, прерывается в первые годы советской власти, что способствует дальнейшей 
  фольклоризации жанра. Этот новый городской романс, вырастающий главным образом 
  из литературной <em>русской песни</em>, но до известной степени являющийся наследником 
  и «классической» народной баллады, не признает «далеких экскурсов в историю 
  &lt;…&gt; и если появляются в нем персонажи из русской истории, то поставлены 
  они в большинстве случаев в сферу любовных или семейно-бытовых отношений» [Гудошников1990, 
  с. 34]; в равной мере не выражена в ней и этническая (или этноконфессиональная) 
  топика.<br>
  <br>
  До некоторой степени городская песня XX в. сохраняет тематический диапазон своих 
  далеких жанровых предков, светского <em>канта</em> и <em>российской песни</em> 
  — элегической (даже пасторальной) любовной, особенно «любовной городской», веселой 
  застольной, шуточной, пародийной (в современной фольклорной песне можно найти 
  аналоги всем этим группам). Сохраняет она и характерный для поэзии второй половины 
  XVIII в. прием создания новых текстов «на голос», т. е. на мелодию известных 
  романсов или «простонародных» песен [Гусев 1988, с. 16]. Как можно убедиться, 
  этот прием весьма активно используется городским фольклором, порождая целые 
  каскады перетекстовок какой-либо особо полюбившейся песни (например, «Маруся 
  отравилась», «Гоп-со-смыком», «Кирпичики» и др. [Неклюдов 2005; 2006]). По-видимому, 
  развитием той же тенденции является «перекличка» текстов мещанского романса 
  XIX в. — в виде различного рода «ответов» либо других песенных рефлексов на 
  тот или иной известный текст [Петровский 1997, с. 43-48]; данный прием также 
  усваивается городской песней XX в., оставаясь весьма продуктивным.<br>
  <br>
  Следует добавить, что подобные песни недолговечны, они появляются и уходят в 
  небытие в течение жизни одного поколения, а то и быстрее. Тексты-долгожители, 
  возникшие еще в XIX в., крайне редки. Анализ содержания песенников той эпохи 
  показывает: из сотен произведений, входивших в массовый репертуар последних 
  десятилетий позапрошлого века, до нашего времени в активном бытовании удержались 
  лишь единицы (речь идет именно о «спонтанном» фольклорном исполнении, а не о 
  сохранении на концертной эстраде, что, впрочем, также происходит не столь часто).<br>
  <br>
  Итак, происхождение городских песен — литературное, точнее, индивидуально-авторское; 
  они становятся анонимными и обрастают вариантами в процессе последующей фольклоризации. 
  Пример такой переработки — фольклоризация стихотворения Я.П. Полонского «Подойди 
  ко мне, старушка…» («Гаданье», 1856), фиксируемого в песенниках с начала XX 
  в. [Гусев 1988, с. 441], а в устной традиции существующего как широко варьируемый 
  анонимный народный романс (Табл. I). В фольклорных редакциях сохраняется сюжетный 
  контур первой половины авторского текста, воспроизводимого довольно близко к 
  прототипу (строфы 2-5), однако мотив предсказанной и ожидаемой в будущем измены 
  (<em>Берегись, тебя твой милый / Замышляет обмануть &lt;…&gt; Говорит она: «Обман 
  твой / Я предвижу — и не лгу»</em>) разворачивается за счет включения поэтических 
  формул из другой, тематически сходной песни, в качестве сюжетной мотивировки 
  предлагающей подробную до полной избыточности характеристику «друга» (как выясняется 
  в финале, ложную): <em>Он любит девушек богатых, / Играет в карты, пьет вино, 
  / Тебя, бедняжку, он загубит, / Ему, бродяге, все равно</em> (вар. III: «Напрасно, 
  девица, страдаешь…» [Кулагина, Селиванов 1999, № 146; Тамаркина 2000, № 16.1-3]). 
  Фольклоризованная редакция стихотворения Полонского контаминируется с этой песней, 
  которая замещает всю его вторую половину (строфы 8-10). В результате мотив «гибели» 
  (у Полонского: <em>Берегись меня! — я знаю, / Что тебя я погублю…</em>; ср. 
  в «Скифах» Блока: <em>Забыли вы, что в мире есть любовь, / которая и жжет, и 
  губит</em>) реализуется буквально — фольклор не любит недоговоренностей: девица 
  пытается отравиться ядом, закалывается лежащим на столике ножиком или умирает 
  от чахотки, после чего обстоятельно и со вкусом описываются похороны. К этой 
  части рассмотренной песни близок текст более позднего (в песенниках с 1912 г.) 
  и весьма популярного романса «Маруся отравилась» [Мордерер, Петровский 1997, 
  № 318].<br>
  <br>
  Наконец, внесюжетная вступительная строфа в варианте 2 скорее всего восходит 
  к блатной традиции, о чем свидетельствует употребление слова <em>мальчик</em> 
  применительно к взрослому мужчине, характерное именно для уголовной среды. «Скажите, 
  пожалуйста: маль-чи-шеч-ка», — говорит проститутка о воре, любовнике товарки 
  в повести «Яма», материал для которой А.И. Куприн собирал в 1890-е годы; ср. 
  приводимые там же песенки: <em>Погиб я, мальчишка, / Погиб навсегда, / А годы 
  за годами — / Проходят лета или: Наутро мальчишку / В сыскную ведут… </em>[Куприн 
  1958, с. 21, 147]. Первая строка этого вступления встречается и в блатной частушке: 
  <em>Дайте ходу пароходу: / Жучка козырем идет, / В нашем лагере погоду / Эта 
  жучка задает</em> (<em>жучка</em> ‘воровка’, ‘подруга вора’) [Джекобсон 2001, 
  с. 272].<br>
  <br>
  Вернемся к вопросу об авторстве прототипических текстов. Он однозначно решается 
  лишь тогда, когда в качестве источника фольклорной песни опознается сочинение 
  профессионального или полупрофессионального литератора — не обязательно широко 
  известного; эти источники достаточно полно выявлены специалистами [Гусев 1988, 
  I-II (прим.); Мордерер, Петровский 1997 (прим.) и др.]. Иногда создатель песни 
  бывает известен исследователям, но авторский первотекст отсутствует в принципе 
  — песня сразу создается в устной форме и неизбежно варьируется от исполнения 
  к исполнению самим автором. В остальных случаях (а их подавляющее большинство) 
  нет никаких надежд обнаружить ни сам первотекст, ни его сочинителя, хотя совершенно 
  очевидно, что все без исключения городские песни имеют индивидуально-авторское 
  происхождение — кем бы ни были эти безвестные авторы. При этом обычно неясно, 
  каким именно путем книжное произведение попадает в фольклор и кем делается первое 
  редактирование литературного прототекста (отбор и перекомпоновка строф и строк, 
  их «дописывание» и т. п.), превращающее авторское стихотворение в фольклорную 
  песню. И уж совсем неуловимы точки «ветвлений» традиции, приводящих к возникновению 
  новых, прежде всего, сюжетных, версий ранее фольклоризованного произведения.<br>
  <br>
  <br>
  <strong>2.</strong><br>
  <br>
  Рассмотрим подробнее процесс фольклоризации литературного произведения на примере 
  народной баллады о Громобое. Прототипическое стихотворение В.А. Жуковского (первая 
  часть поэмы «Двенадцать спящих дев. Старинная повесть в двух балладах» [1810]) 
  рассказывает о грешнике, заключившем договор с дьяволом и продавшем ему за земные 
  блага свою душу. Этот сюжет восходит к прозаическому роману Х.-Г. Шписа «Двенадцать 
  спящих дев, история о приведениях», однако действие перенесено в Древнюю Русь. 
  Имя Громобой Жуковский заимствовал из «героической повести» Г.П. Каменева «Громобой» 
  (1796); впрочем, еще раньше оно появилось в одной из «Русских сказок» В.А. Левшина 
  — «Повести о дворянине Заолешанине, богатыре, служившем князю Владимиру» (1780), 
  где так звался отец героя [Гистер 2005, с. 44]. Во всех случаях это искусственно 
  сконструированное имя должно было звучать как характерное для Киевской Руси.<br>
  <br>
  Таким образом, если выше говорилось о «внеисторичности» народной баллады, то 
  здесь мы могли бы ожидать некоторого исключения из правила. Однако у Жуковского 
  указание на «историчность» сюжета, впрочем, весьма условную, обнаруживается 
  лишь раз: упоминание бояр, витязей и князей, с которыми дьявол предлагает свести 
  героя. Фольклор удерживает этот признак и даже добавляет деталь, отсутствующую 
  в прототипическом произведении: меч, которым Громобой надрезает себе руку, чтобы 
  дать дьяволу кровавую расписку.<br>
  <br>
  На данный сюжет композитором А.Н. Верстовским была создана опера (первая постановка 
  — Москва, январь 1857), существовали многочисленные лубочные переработки стихотворения 
  Жуковского (например: Громобой. Романтическая баллада [анонимная, б.г., б.м.]; 
  К. Г&lt;олохвасто&gt;в. Громобой. Новгородский витязь и двенадцать спящих дев. 
  1915 [возможно и раньше] и др.)[2], печаталась баллада о Громобое и в песенниках 
  [Гусев 1988, т. I, с. 220]. Вероятно, именно через подобные издания она проникла 
  в фольклорный репертуар, причем приоритет здесь скорее принадлежал лубочным 
  текстам, в то время как песенники могли фиксировать также и уже сложившуюся 
  традицию ее исполнения.<br>
  <br>
  Нет никакого сомнений, что именно в таких массовых изданиях осуществлялась первичная 
  адаптация текста для его освоения социальными слоями «третьей культуры»[3], 
  а дальнейший процесс фольклоризации протекал уже в самой устной традиции. С 
  этой точки зрения всю цепочку трансмиссии — от неизвестной нам лубочной переработки 
  до сохранившихся устных вариантов — можно рассматривать в рамках одной «постфольклорной» 
  традиции. Остается неясным, какую роль здесь сыграла опера Верстовского, очевидно, 
  впервые давшая сюжету «звучащую», музыкальную форму. Она могла быть и достаточно 
  значительной, особенно если учесть характерное для этого композитора использование 
  в оперном и вокальном творчестве народных и романсовых мелодий, что тоже должно 
  было способствовать фольклоризации стихотворения.<br>
  <br>
  Сюжет о договоре с дьяволом отнюдь не чужд русской словесности — как книжной, 
  так и устной. В первую очередь имеется в виду, конечно, «Повесть о Савве Грудцыне» 
  (вторая половина XVII в.) [Скрипиль 1935, с. 181-214; 1936, с. 99-152; 1954, 
  с. 191-214], а также ряд менее известных и менее значительных произведений XVII-XVIII 
  в. («Слово и сказание о некоем купце», «Повесть о некоем убогом человеке», «Повесть 
  о некоем богоизбранном царе», «История о пане Твердовском») [Журавель 1996]. 
  В «Повести о Савве Грудцыне» есть выразительные совпадения с балладой Жуковского 
  (не говоря уж о самой центральной теме, общей для обоих произведений). Это мотивы 
  расписки и поклона дьяволу (впрочем, универсально входящие в ритуал договора 
  с ним): <em>И обольстителю душой / За злато поклонился./ Разрезав руку, написал 
  / Он кровью обещанье; Лукавый принял — и пропал…</em> («Громобой») — <em>Пришед 
  же Савва пред царя оного, пад на землю, поклонися ему &lt;…&gt; подносит ему 
  богоотметное свое писание…</em> («Савва Грудцын»), но прежде всего — речи дьявола, 
  выдающего себя за друга и брата героя, предлагающего именно дружескую и братскую 
  помощь: <em>И грудью буду я стоять / За друга и за брата</em> («Громобой») — 
  <em>…ныне убо буди брат и друг и не отлучаися от мене, аз бо всякое вспоможение 
  во всем рад чинити тебе</em> («Савва Грудцын»); более отдаленной и, возможно, 
  не показательной параллелью является упоминание призываемых на помощь герою 
  «днепровских вод» (в разной функции): <em>в помощь вас зову, / Днепровски быстры 
  воды</em> («Громобой») — <em>Бес же и Савва &lt;…&gt; прибегоша к реке Днепру, 
  и абие расступися им вода</em> («Савва Грудцын»).<br>
  <br>
  В фольклоре данная тема разрабатывается в рамках сюжетного типа «Человек продает 
  свою душу дьяволу» [AaTh 1170-1199], однако в центре этих сказочно-новеллистических 
  разработок (как в фольклоре мировом [AaThUth, Pt II, p. 60-71], так и в восточнославянском 
  [СУС, с. 271-272]) обычно стоит мотив хитрости, позволяющей герою избежать финальной 
  расплаты (часто с помощью его умной жены). Показательна, например, сказка о 
  попе, который хотел разбогатеть и продал душу дьяволу, заключив договор на десять 
  лет. Когда пришел срок, попадья в свою очередь заключила с чертом еще один договор 
  на десять лет — с условием, что в конце он должен будет выполнить три ее желания 
  и только после этого получит обе души, а если нет, то откажется от своих притязаний. 
  «Является черт. “Ну, — говорит, — скорее задавай первое дело”. — “А вот тебе 
  первое дело”, — говорит попадья...». Как далее замечает собиратель, содержание 
  этих трудных задач «не для печати; черт их не выполняет» [Новиков 1961, № 69]. 
  Скорее всего, сказка сложена с опорой на одну из лубочных переработок «Громобоя» 
  Жуковского. Об этом говорит, во-первых, прямая ссылка на нее (<em>Повалило попу 
  счастье, как громобою</em>), а во-вторых заимствованный, вероятно, оттуда же 
  мотив пролонгации договора с чертом — после условленных десяти лет еще десять, 
  что, кстати, исключает возможность влияния на сказку фольклоризованных редакций 
  баллады, в которые эпизод подобного продления не вошел, оставшись за их фабульными 
  рамками. В них срок договора обычно вообще не упоминается, он назван только 
  в варианте IV, причем в суммированном виде: <em>И ровно-ровно двадцать лет / 
  Ты будешь жить богато &lt;...&gt; Ты будешь счастлив двадцать лет / Я слова 
  не нарушу,/ А ровно через двадцать лет / Ты мне подаришь душу!</em><br>
  <br>
  Вернемся к устным вариантам «Громобоя». Их известно немного — последующий анализ 
  строится на четырех выявленных текстах (см. Табл. II)[4]. Из-за полного несоответствия 
  данного сюжета тематическому кругу фольклорных песен «Громобой» никак не относится 
  к числу популярных баллад, тем более поздних, среди которых он стоит особняком; 
  скорее приходится удивляться тому, что он все-таки удержался в устном репертуаре. 
  Не исключено, что свою роль здесь могла сыграть, с одной стороны, распространенность 
  упомянутых лубочных переработок, а с другой, популярность сюжетно близкой «Повести 
  о Савве Грудцыне», дожившей в крестьянской и старообрядческой письменности до 
  XX в. [Журавель 1996, с. 4, 8-9].<br>
  <br>
  Даже при некоторых расхождениях фольклорных редакций все они включают примерно 
  один и тот же корпус строф, имеющих соответствия в первотексте. Такими общими 
  для всех редакций являются фрагменты 1, 5, 6, 7, 11, 12, 14, которые составляют 
  минимальный набор, сохраняющий сюжет баллады; полнее всего этот набор представлен 
  в варианте I. Сюда следует добавить фрагм. 4, в котором говорится о появлении 
  перед героем «привиденья» (вар. I-III) — ему соответствует вторая строка фрагм. 
  5 в варианте IV, заключающая в себе ту же информацию. Фрагменты 13 (вар. I-II) 
  и 16 (вар. III-IV) по существу представляют собой две альтернативные редакции 
