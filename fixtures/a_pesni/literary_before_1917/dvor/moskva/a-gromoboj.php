  <br>
  Однако скорее это свидетельствует лишь о том, что сама баллада Жуковского продолжала 
  сохранять свое значение «контролирующей инстанции», с которой время от времени 
  сверялась традиция, заимствуя оттуда фрагменты, замещающие те, что вошли в устный 
  текст раньше, и поддерживающие тот же сюжет.<br>
  <br>
  Надо заметить, что при чрезвычайной близости фольклорных переработок к тексту 
  оригинала в нем есть только две строки, которые воспроизводятся песенной традицией 
  совершенно точно (<em>Утесы под ногами…</em> [вар. III] и С блестящими глазами… 
  [вар. I]), все остальные стихи оказываются в той или иной степени измененными. 
  Это изменения порядка слов (по-видимому, вызываемые потребностями мелодической 
  формы), замены ряда архаизмов и вообще лексики, относящейся к «высокому» стилю 
  поэтического языка (<em>стремнина, окрест, прянуть, обольститель, злато</em>), 
  то же касается некоторых литературных оборотов, архаических или синтаксически 
  и интонационно тяжеловесных для устной формы (<em>В дугу сомкнутый над клюкой…; 
  К боярам, витязям, князьям / Тебя введу я в дружбу...; Но десять лет — не боле 
  — срок / Тебе так жить богато... ; Клянусь... свидетель ада бог…</em>).<br>
  <br>
  Само описание дьявола — горбатый старик с всклокоченной («шершавой») бородой, 
  «с хвостом, когтьми, рогами» — скорее соответствует его иконографии, восходящей 
  к европейскому средневековью. Скажем, следуя этой традиции, Гюго в «Легенде 
  о Пекопене» (имеющей своим источником немецкие предания) описывает дьявола в 
  виде «старого сгорбленного старика» — он появляется перед героем как «хромой 
  и горбатый старикашка, [который] был очень безобразен» [Гюго 1919, с. 33, 41, 
  56, 58, 93]. Подобный облик удерживается устными вариантами баллады: бородатый 
  / седовласый старик (вар. I, III, IV), с хвостом и рогами (вар. I), с когтями 
  и рогами (вар. II, III); эти зооморфные атрибуты вполне соответствуют и восточнославянским 
  представлениям о черте (покрыт шерстью, с рогами, хвостом и копытами [Петрухин 
  2002, с. 484]), тогда как старческий облик для него здесь не столь характерен, 
  хотя и встречается: так, согласно одному рассказу, он «страшный, седой с рогами» 
  [Новичкова 1995, с. 583].<br>
  <br>
  Примечательно, что в этом фрагменте (появление дьявола) фольклорная традиция 
  производит не столь заметную, но достаточно значимую корректировку текста. У 
  Жуковского демон выходит из леса (<em>из темной бора глубины</em>; кстати, Пекопен 
  у Гюго тоже встречает дьявола в лесу), тогда как в устных редакциях баллады 
  черт появляется из глубины вод: <em>Хотел он броситься на дно, /.../ А там, 
  во темной глубине, / Он видел привиденье</em> [вар. I]; <em>И вдруг из моря 
  глубины / Выходит привиденье</em> [вар. II]; <em>Из темной буйной глубины / 
  выходит привиденье </em>[вар. III]. Таким образом, если в первом случае демон 
  скорее уподобляется лешему, то во втором — водяному. Вообще, чертом может быть 
  названа любая нечисть [Новичкова 1995, с. 577], но все же чаще так именуется 
  водяной, дух гораздо более опасный и враждебный людям, чем леший. Именно болота, 
  омуты, озера без дна считаются прямыми ходами в царство Сатаны, их-то и населяют 
  черти [Новичкова 1995, с. 577, 579, 581, 588]. Не последнюю роль в этом уподоблении 
  играет и облик собеседника Громобоя, более приличествующий водяному, который 
  может предстать древним длиннобородым и лохматым стариком, обросшим всклокоченной 
  шерстью [Левкиевская, Усачева 1995, с. 396; Новичкова 1995, с. 96].<br>
  <br>
  Наконец, фольклор уточняет и дополняет перечень дьявольских посулов. У Жуковского 
  он занимает не менее четырех строф, между которыми располагаются пространные 
  увещевания демона и его хвала аду — эти части традицией опускаются (возможно, 
  из суеверных опасений). Кроме того, в устных редакциях устраняются повторы — 
  скажем, на месте двукратного обещания богатства (<em>Могу тебе я силу дать / 
  И честь я много злата &lt;…&gt; И вот в задаток кошелек: / В нем вечно будет 
  злато</em>) во всех вариантах остается одно (мотив неразменного кошелька). Предложение 
  «дружбы» высокородных особ (<em>бояр, витязей, князей</em>) воспроизводится 
  только один раз (вар. III), однако на основе этой строчки формируется другой 
  посул: <em>Еще боярышню-княжну, / Сведу с тобой я дружбу</em> (вар. II). Оно 
  замещает менее конкретное обещание: <em>Досель красавиц ты пугал — / Придут 
  к тебе толпою... </em>(у Жуковского), которое в фольклорной версии разворачивается 
  следующим образом: <em>Еще даю тебе коня, / Хрустальную карету, / В ней будет 
  девушка одна, / На свете краше нету</em> (вар. IV); <em>Я дам девчонку юных 
  лет, / Которой краше нету </em>(вар. I).<br>
  <br>
  Вообще новаций становится больше к концу устных вариантов (в Таблице II они 
  подчеркнуты). Кроме названных выше строк к ним относятся заключительная сентенция 
  дьявола (фрагм. 16, вар. III и IV) — вместо лаконичного «До свиданья!» у Жуковского, 
  а также резюме повествователя: <em>А Громобой с своей душой / Навеки распрощался</em> 
  (вар. I); <em>Пропал навеки Громобой, / Он бесу поклонялся</em> (вар. II), представляющее 
  собой переработку строк прототекста <em>И обольстителю душой / За злато поклонился.</em><br>
  <br>
  Наконец, насколько это возможно, устные редакции выдерживают стилистический 
  регистр, определенный текстом Жуковского, за единственным, пожалуй, исключением. 
  Речь идет о строчках: <em>Взял в руки финское перо / И кровью расписался</em> 
  (вар. IV), испытавших некоторое влияние блатной лирики. <em>Финское перо</em> 
  — финский нож, финка, часто фигурирует в уличных песнях (<em>В кармане — финский 
  нож; За нее пускали финки в ход; И не думай, что я замахнусь / В грудь чужую 
  финляндским ножом... </em>и т. д.), в том числе встречается и в более редкой 
  форме <em>финское перо</em> (<em>Имел ключи, имел отмычки, / Имел я финское 
  перо</em> [Джекобсон 1998, с. 33]); уподобление же ножа перу (возможно, от сочетания 
  <em> перочиный нож</em>) фиксируется по крайней мере с начала XX в. [Грачев, 
  Мокиенко 2006, с. 136-137].<br>
  <br>
  <br>
  <strong>3.</strong><br>
  <br>
  Итак, готовность фольклора принять и адаптировать произведение «высокой» поэзии 
  находится в прямой зависимости от присутствия «вакантных» мест для возникающей 
  новации, т.е. существования в его культурном «гено-тексте» неких «встречных 
  течений». Согласно А.Н. Веселовскому, сходное притягивается сходным, что предполагает 
  обязательное наличие аналогий между воспринимаемой и воспринимающей традицией, 
  причем аналогичные элементы в воспринимающей традиции могут быть иначе структурированными, 
  фрагментированными, вообще слабо проявленными и т. д. Заимствование мобилизует 
  как эти вакансии для новообразований, так и дополнительные возможности воспринимающей 
  традиции, позволяющие переорганизовать имеющийся в ее распоряжении фрагментарный 
  материал (формулы, мотивы и пр.), рождая тем самым новое качество. Так, возможность 
  фольклоризации стихотворения Жуковского была обусловлена наличием в русской 
  традиции темы договора с дьяволом (известная в крестьянской и старообрядческой 
  среде «Повесть о Савве Грудцыне», народные сказки сюжетного типа AaTh 1170-1199), 
  а также ряда демонологических представлений, которые позволили по-своему понять 
  фигуру демона-искусителя из прототипического текста, имеющую свое происхождение 
  скорее в образном строе западноевропейского романтизма.<br>
  <br>
  Как правило, литературный текст в своем «исконном» виде недостаточно пригоден 
  для последующей фольклоризации, он должен быть каким-то образом адаптирован 
  для нее. Пути этой адаптации различны (скажем, «Конек-горбунок» П.П. Ершова 
  и «Сказка о царе Салтане» А.С. Пушкина приходят в бразильскую парафольклорную 
  «веревочную литературу» через посредство детских прозаических переработок [Пирес 
  Феррейра 1999, с. 30-31]). В России основной формой такой адаптации являются 
  лубочные и другие массовые издания, которые предлагают устной традиции упрощенные 
  версии текстов зарубежной словесности и литературной классики. При этом весь 
  спектр устных вариантов в конечном счете, вероятно, часто сводится к одной прототипической 
  переработке, которую и усваивает фольклор. Это не исключает последующих, «вторичных» 
  влияний на уже сложившуюся устную версию «исходного» литературного текста — 
  следы таких влияний могут быть обнаружены в виде альтернативно используемых 
  формул, восходящих к одному и тому же авторскому оригиналу. Вообще реконструкция 
  некоего «узкого» книжного источника для целого набора фольклорных вариантов 
  в ряде случаев является вполне уместным приемом фольклорной текстологии (позволяющим, 
  например, выявить прототипические редакции для ряда бурятских версий эпоса о 
  Гесере [Неклюдов 1984, с. 203-204]).<br>
  <br>
  Используемый фольклором письменный текст практически никогда не остается совершенно 
  неизменным в устной традиции. Попав в нее и тем самым войдя в режим постоянного 
  варьирования, он становится пластичным и непременно начинает предлагать свои 
  редакции строк и фрагментов авторского оригинала. Так, вместо строчек <em>Стал 
  думу думать Громобой, / Подумал, согласился</em> (у Жуковского) фольклор дает 
  свои версии данной фразы, казалось бы, альтернативные, но на самом деле контекстуально 
  синонимичные[5]: <em>И долго думал Громобой, / И долго не решался… </em>(вар. 
  I) — <em>Недолго думал Громобой, / Он тут же согласился </em>[<em>Недолго сомневался…, 
  Не долго колебался…</em>] (вар. II-IV). Аналогичному варьированию подвергаются 
  и прочие вошедшие в фольклор строфы баллады Жуковского.<br>
  <br>
  Жанр городской песни диктует и определенный объем текста (как было сказано, 
  примерно 4-10 строф; исключения относительно редки). В силу этого литературные 
  тексты в устной традиции обычно подвергаются сокращению (за счет несвойственных 
  фольклору описаний пейзажа и душевных состояний героев, пространных диалогов 
  и т. п.). Случаются, правда, и дополнения — например, фабульное развитие «Гаданья» 
  Полонского с помощью строф романса «Напрасно, девица, страдаешь», или две заключительные 
  строфы в устных версиях «Элегии» А.Н. Амосова, дающие ее гармоническое сюжетное 
  завершение (убийство Хас-Булата и самоубийство князя, снимающую неопределенность 
  авторской концовки стихотворения). Однако все это происходит в рамках разрешенного 
  жанром размера текста.<br>
  <br>
  Наконец, вариантные изменения текста в устной традиции обычно минимальны в его 
  начале и в наибольшей степени проявляются в его второй половине и в финале[6]. 
  Подобное увеличение амплитуды варьирования, нарастание пластичности произведения 
  к его концу, по-видимому, является универсальной закономерностью и затрагивает 
  все уровни фольклорного текста — его объема, его формы и содержания (развязка 
  одного и того же сюжета может по вариантам весьма существенно различаться).<br>
