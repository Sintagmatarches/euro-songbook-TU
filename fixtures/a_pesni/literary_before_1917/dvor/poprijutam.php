  <br>
  1. По приютам я с детства скитался</strong><br>
  <br>
  <em>Популярнейшая песня питерских босяков и беспризорников. Позже распространилась 
  по всей России. Была известна ещё до революции примерно в том виде, в котором 
  приводится ниже. Самое раннее упоминание мне удалось найти в «Республике ШКИД» 
  Пантелеева и Белых (1927 г.) Во времена гражданской войны и нэпа песня обрастала 
  всё новыми куплетами и подробностями, которые приводятся курсивом – с соответствующими 
  пояснениями.<br>
  </em><br>
  ***<br>
  По приютам я с детства скитался,<br>
  Не имея родного угла.<br>
  Ах, зачем я на свет появился,<br>
  Ах, зачем меня мать родила?!<br>
