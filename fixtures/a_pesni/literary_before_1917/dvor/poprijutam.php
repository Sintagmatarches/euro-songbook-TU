  Ах, зачем меня мать родила?!<br>
  <br>
  <em>Две последние строки повторяются<br>
  <br>
  (1) Шмонать – здесь: лазать по карманам, воровать (вообще «шмонать» - обыскивать; 
  от «шмон» - обыск).<br>
  (2) Поздний вариант – «По чужим, по буржуйским карманам».<br>
  (3) Литовский замок – тюремный замок в Санкт-Петербурге. Пятиугольное серо-казенное 
  здание названо так по квартировавшему там в свое время Литовскому мушкетерскому 
  полку. Замок украшали семь башен, оттого он также назывался Семибашенным. С 
  1823 года мрачные серые помещения замка стали использоваться в качестве следственной 
  тюрьмы. Чаще всего – для уголовников, но содержали здесь и политических (о чём 
  вспоминает Короленко в «Истории моего современника»). В марте 1917 года Литовский 
  замок разрушен и сожжен. Развалины сохранялись до 30-х годов, потом были окончательно 
  разобраны. Ясно, таким образом, что песня возникла до 1917 года. В более поздних 
  переработках строка про Литовский замок звучит в редакции – «За решётку меня 
  закатал».<br>
  (4) Куплет появился не раньше начала 30-х годов, поскольку до этого так называемого 
  «воровского закона» не существовало, равно как самих «воров в законе».<br>
  (5) Хозяин – начальник тюрьмы.<br>
  (6) Куплет появился не ранее 1933 года, поскольку строительство Магадана началось 
  в 1933 году, в устье ручья того же названия, а лагеря в бассейне реки Воркуты 
  (Заполярье, правый приток Усы) появились в 1932 году, когда Ухтпечлагом стали 
  здесь разрабатываться угольные копи.<br>
