  Известна под разными названиями, в том числе <strong>&quot;Позабыт-позаброшен&quot;</strong>. Была популярна у беспризорников и уголовников. В основе своей дореволюционная, но широко бытовала и в середине XX в. Михаил Демин в автобиографическом романе &quot;Блатной&quot;  вспоминает ее пение в тюрьме Конотопа в июле 1947 г., харакетризуя как &quot;старую бродяжью&quot;: &quot;Вот умру я я, умру я...&quot; (Демин М. Блатной. М.: Панорама, 1991. С. 7. Роман написан в Париже в 1969-72 гг.). См. близкую песню и несчатной любви <a href="vkalin.php">"В калиновой роще"</a>. У песни оригинальный размер - большинство вариантов ориентируются на анапест, но в третьей строке четверостишия обычно добавляется третий безударный слог между ударными, так что это скорее логаэд:<br>
