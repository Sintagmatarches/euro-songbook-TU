  Зачем ты ходишь пред тюрьмою, <br>
  Зачем ты мучаешь меня? <br>
  Ведь ты гуляешь с всеми, с кем попало, <br>
  Совсем забыла про меня.<br>
  <br>
  Сижу день цельный за решёткой,<br>
  В окно тюремное гляжу,<br>
  А слёзы катятся, братишки, потихоньку,<br>
  По исхудалому мойму лицу.<br>
  <br>
  Сижу я цельный день в халате,<br>
  На нём сплошные рукава,<br>
  Фуражка новая на вате, (1)<br>
  Чтоб не промёрзла голова.<br>
  <br>
  Хотел бы голыми руками<br>
  Я цепь железную порвать,<br>
  Да жаль, братишечки, я с кандалами,<br>
  Мне всё одно не убежать.<br>
  <br>
  Меня заметят часовые,<br>
  И крикнут раз, и крикнут два,<br>
  Взведут курки они стальные,<br>
  Наверное, убьют меня.<br>
  <br>
  Так зачем ты ходишь пред тюрьмою…и т. д.<br>
