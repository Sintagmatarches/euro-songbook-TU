  1. Сижу я цельный день, скучаю</strong><br>
  <br>
  Сижу я цельный день, скучаю,<br>
  В окно тюремное гляжу,<br>
  А слезы катятся, братишки, постепенно<br>
  По исхудалому лицу.<br>
  <br>
  - Зачем ты, милый, меня бросил,<br>
  Зачем же бросил ты меня?<br>
  Сидишь теперь в своей тюряге<br>
  И позабыл да ты уже меня.<br>
  <br>
  - Чего ж ты ходишь пред тюрьмою,<br>
  Зачем ты мучаешь меня?<br>
  Катись ты, милая, куда-нибудь подальше<br>
  И уходи ты от меня.<br>
  <br>
  - Сидит мой милый весь в халате,<br>
  На ём сплошные рукава,<br>
  Шапчонка у него на вате,<br>
  Чтоб не болела голова.<br>
  <br>
  - Так не ходи ж ты пред тюрьмою<br>
  И не греми подборами,<br>
  Катись ты, милая, куда-нибудь подальше<br>
  С такими разговорами.<br>
