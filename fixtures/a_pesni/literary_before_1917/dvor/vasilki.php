 <br>
  <br>
  Городской романс на основе стихотворения Александра Апухтина «Сумасшедший» («Садитесь, 
  я вам рад. Откиньте всякий страх…», &lt;1890&gt;, см. в конце страницы) – это 
  то самое стихотворение, которое пыталась прочитать на приемной комиссии героиня 
  Ирины Муравьевой в фильме «Карнавал». Стихотворение было положено на музыку 
  для мелодекламации Н. Киршбаумом, а фрагмент его стал романсом &quot;Васильки&quot; 
  на музыку неизвестного автора. Подробнее о песне и ее вариантах см. А. Архипова. <a href="moskva/a-kakola.php">Как погибла Оля и родился фольклор</a> // Кирпичики. Фольклористика и культурная антропология сегодня. М.: РГГУ, 2008.  С. 432-455.<br>
