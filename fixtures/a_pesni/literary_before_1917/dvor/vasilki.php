  <span class='bold'>5. Васильки </span><br>
  <br>
  <span class='i'>Об этой песне хотелось бы особо сказать несколько слов. Я услышала ее в 
  первый (и единственный) раз от пятнадцатилетней деревенской девушки, которая 
  приехала на заработки в Москву и устроилась няней к ребенку соседей. На дворе 
  стоял 1954 год. <br>
  <br>
  От первой до последней строки она сразу - и навсегда - запала мне в память. 
  Поразила, видать, детское воображение. Много позже я обнаружила, что "первотолчком" 
  для ее создания могли стать строки стихотворения А. Н. Апухтина (1840-1893) 
  "Сумасшедший". Потом, правда, сюжет потек по другому руслу. В дальнейшем эта 
  песня встретилась мне в одном из сборников. Там она была более последовательной 
  и логичной, чем "моя". Но в этой книге я привожу тот первый, несколько загадочный 
  вариант, который запомнила с детства. </span><br>
