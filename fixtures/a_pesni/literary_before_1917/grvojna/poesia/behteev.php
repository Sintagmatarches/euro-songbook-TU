	<span class="header"><strong>СЕРГЕЙ 
  БЕХТЕЕВ (1879-1954)</strong><br></span>
  <br>
        <strong><br>
        Сергей Сергеевич Бехтеев</strong> (7.4.1879, Елец — 4.05.1954, Ницца) 
        - поэт, монархист, участник Белого движения, белоэмингрант. Окончил Императорский 
        Александровский лицей. В 1903 издал первый сборник стихов (с посвящением 
        императрице Марии Федоровне). Служил в Кавалергардском полку. С 1914 — 
        на фронте. Февральскую революцию застал на отдыхе на Кавказе. С 1918 — 
        в Добровольческой армии. В ноябре 1920 г. покинул Крым с войсками Врангеля. 
        В эмиграции в 1920-29 гг. в Сербии, с конца 1929 — в Ницце. В эмиграции 
        издал несколько сборников стихов и автобиографический роман в стихах &quot;Два 
        письма&quot; (1925). <br>
  <br>
        В сб. Антология военной песни. Сост. и автор предисл. В. Калугин. М.: 
        Эксмо, 2006, - он охарактеризован так: <em>&quot;Надпись на его надгробии 
        на русском православном кладбище во французском городе Ницце гласит: "Сергей 
        Сергеевич Бехтеев. 7.04.1879 - 4.05.1954. Лицеист 59 выпуска Императорского 
        Александровского лицея. Царский поэт. Офицер Белой армии. Корнет". И в 
        этой краткой надписи сказано все о последнем лицеисте и последнем "Царском 
        гусляре". Его "Молитва", обнаруженная в Ипатьевском доме после расстрела 
        царской семьи, стала легендарной. Вся поэзия Бехтеева - это песня-молитва, 
        его стихи звучали в Русском Зарубежье под гитару&quot;. </em><br>
