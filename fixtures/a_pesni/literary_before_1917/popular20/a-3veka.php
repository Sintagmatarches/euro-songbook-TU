  <br>
  Показательна в этом отношении песня под названием "Весна". Нет предела весеннему 
  ликованию:<br>
  <br>
  Бери мешок и фляжку! <br>
  Вперед, товарищ, в путь! <br>
  Рубашка нараспашку <br>
