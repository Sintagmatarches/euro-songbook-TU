  Но и на этой песне проставлена дата подписания в печать: "22.VII.42". Песня 
  создана через год после начала войны. <br>
  <br>
  Самое же удивительное состоит в том, что все так и произойдет: мы вновь разобьем 
  <em>псов-фашистов</em>, наши торпеды будут топить немецкий флот, советский народ 
  пронесет свои <em>боевые знамена</em> по землям врага и даже весна Победы окажется 
  такой же, но в 45-м, а не в 42-м. Поэтическое слово обладает способностью предвидеть 
  события, но здесь перед нами, увы, прямые свидетельства того неадекватного состояния, 
  которое в первые месяцы пришлось преодолевать не только поэтам-песенникам, но 
  и самому Верховному главнокомандующему. Иными песни и быть не могли. В том не 
  вина, а всеобщая беда начала войны. <br>
  <br>
  Перелом наступил к концу 41-го, после <a href="../ww2/oficial/vzemlanke.htm">"Землянки"</a> Суркова - Листова и <a href="../ww2/oficial/zdimenja.php">"Жди меня"</a> 
