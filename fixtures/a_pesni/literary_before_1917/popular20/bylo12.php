  Сказ о Кудеяре-атамане входит в главу «Пир на весь мир». Некрасов умер 8 января (по новому стилю) 1878 г., оставив поэму 
  неоконченной. Он не ответил на вопрос, кому на Руси хорошо жить. 
  Прототипом пана Глуховского мог быть реальный смоленский помещик середины XIX 
  в. Глуховский, который засек до смерти крестьянина, о чем сообщал «Колокол» 
  Герцена от 1 октября 1859 г. <br>
  <br>
  Главу «Пир на весь мир»  Некрасов пытался опубликовать в «Отечественных 
  записках» за ноябрь 1876 г., а затем за январь 1877 г., оба раза получив отказ 
  цензуры. Глава напечатана посмертно в нелегальном издании Петербургской вольной 
  типографии в 1879 г. В 1881 г. искаженный и урезанный вариант был опубликован 
  в февральском номере «Отечественных записок». Публикация пришлась на пик народовольческого террора, увенчавшегося в марте 
  1881 г. убийством Александра II.<br>

