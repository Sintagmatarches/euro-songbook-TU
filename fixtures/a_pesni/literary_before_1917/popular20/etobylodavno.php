  - Почему ты, ямщик, перестал песню петь.<br>
  И сидишь ты какой-то унылый?<br>
  Колокольчик вдали продолжает звенеть,<br>
  А тебя не слыхать, друг мой милый.<br>
  <br>
  - Это было давно, лет семнадцать назад,<br>
  Вез я девушку трактом почтовым;<br>
  Круглолица, бела, словно тополь стройна<br>
  И покрыта платочком шелковым.<br>
  <br>
  Попросила она, чтоб я песенку спел,<br>
  Я запел, и она зарыдала.<br>
  Кони быстро неслись, словно звери лихи,<br>
  Иль несла их нелегкая сила.<br>
  <br>
  Вдруг жандармский разъезд перерезал нам путь,<br>
  Тройка быстро, как вкопана, стала.<br>
  Кто-то выстрелил вдруг прямо девушке в грудь,<br>
  И она, как цветочек, увяла.<br>
  <br>
  Мне не жалко коня, мне не жалко себя,<br>
  Только жаль ее жизнь молодую.<br>
  Умирая, она говорила слова:<br>
  «Я за правду, за волю страдала».<br>
  <br>
  Посмотрите: вдали там березка стоит,<br>
  И могила заросша травою.<br>
  Под высоким холмом крепко девица спит,<br>
  Унесла она песню с собою.<br>
