  <br>
  Судя по многообразию вариантов, песня была очень популярной на протяжении долгого 
  времени. Есть блатные переработки - см. <a href="../dvor/chelkplatotchek.php">&quot;Шелковый 
  платочек&quot;</a>. Очень часто в песне встречается довольно странная для фольклора 
  дата &quot;лет семнадцать назад&quot; - она совсем не круглая, и даже не &quot;лет 
  15 назад&quot;, что смотрелось бы естественней. А потому можно предположить, 
  что неизвестный автор отталкивался от какого-то конкретного исторического события. 
  Героиня - явно народница, причем вряд ли мирного пропагандистского периода, 
  иначе зачем было бы в нее стрелять без предупреждений. Вероятнее всего, речь 
  идет о народовольческом терроре 1879 - начала 1881 гг. и связанными с ним репрессиях. 
  Тогда предполагаемое время создания песни - около 1897 г., и сочинил ее кто-то 
  из поздних народников, сознательно стилизуя под народную песню. Возможно, героиня 
  - историческое лицо, и песня основана на реальных обстоятельствах ее гибели.<br>
