  <strong>1. Почему ты, ямщик, перестал песни петь? </strong><br>
  <br>
  Почему ты, ямщик, перестал песни петь? <br>
  Не поешь и какой-то унылый. <br>
  Колокольчик один продолжает звенеть, <br>
  А тебя не слыхать, друг мой милый. <br>
  <br>
  &quot;Это было давно, год, примерно, назад, <br>
  Вез я девушку трактом почтовым. <br>
  Круглолица она, словно тополь стройна <br>
  И накрыта платочком шелковым. <br>
  <br>
  Попросила она, чтоб я песню ей спел,<br>
  Я запел и она подхватила.<br>
  Кони мчались стрелой, словно в море волна,<br>
  Как несла их нечистая сила. <br>
  <br>
  Вдруг жандармский разъезд перерезал нам путь, <br>
  Наша тройка как вкопана встала. <br>
  Кто-то выстрелил вдруг прямо в девичью грудь <br>
  И она как цветочек завяла. <br>
  <br>
  Перед смертью она лишь успела сказать: <br>
  &quot;Из тюрьмы я недавно бежала. <br>
  Слишком рано пришлось за народ умирать, <br>
  Ох, как жаль, ято я сделала мало&quot;.<br>
