  Народная песня на основе стихотворения Дмитрия Садовникова &quot;Из-за острова на стрежень...&quot; 
  (1883). Н. Телешов вспоминал, что в широкое обращение ее ввел Скиталец (Степан Петров, р. 1869), исполнявший песню на &quot;Московских литературных средах&quot;в доме Телешова (Телешов Н. Записки писателя. М.: Сов. писатель, 1952. С. 41-42). Скиталец переехал в Москву в августе или сентябре 1902 г., т.е. исполнение им песни на &quot;Средах&quot; не могло произойти ранее. В автобиографической повести Скитальца &quot;Сквозь строй&quot; (1902) песню поет, аккомпанируя на гуслях, отец автора в середине 1870-х гг., когда они  плывут по Волге на Нижегородскую ярмарку на пароходе - но это анахронизм, так как стихотворения еще не было.  <br>
   <br>
   Садовников, фольклорист, этнограф и поэт, создал два цикла стихов о Степане 
  Разине - &quot;Из волжских преданий о Стеньке Разине&quot; и &quot;Песни о Стеньке 
  Разине&quot;. Популярными народными песнями стали &quot;Из-за острова на стрежень&quot; 
  и <a href="poposadu.htm">&quot;По посаду городскому&quot;</a>. Песня входила 
  в репертуар Федора Шаляпина и Надежды Плевицкой. Она легла в основу сюжета первого русского художественного фильма &quot;Понизовая вольница&quot; (режиссер Владимир Ромашков, 1908).<br>
