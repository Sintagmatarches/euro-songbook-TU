  <strong>Дмитрий Николаевич Садовников</strong> (25 апреля (7 мая) 1847, Симбирск — 19 (31) декабря 1883, Санкт-Петербург)<br>
<br>
<br>
  <strong>УТОПЛЕНИЕ КНЯЖНЫ</strong><br>
  <br>
  Утопление княжны якобы произошло в Астрахани осенью 1669 
  года, когда Разин вернулся из похода в Персию &quot;за зипунами&quot;, во время 
  которого якобы была пленена прекрасная персидская княжна. Менее чем через год 
  Разин поднимет на Дону восстание и летом 1670 года опять окажется в Астрахани - 
  захватив город силой.<br>
  <br>
  Впервые сцена описана голландским ремесленником и путешественником 
  Яном Янсеном Стрейсом (или Стрюйсом, Struys, 1630-1694) в его книге «Три путешествия», 
  изданной в 1676 г. в Амстердаме и впоследствии переведенной на многие языки 
  (на русском вышла в Москве в 1935 г.). Стрейс с 1668 года работал парусным мастером 
  в России и находился в Астрахани во время разинского бунта. В 1824 в журнале 
  «Северный Архив» в статье А. Корниловича о путешествии Стрейса был воспроизведен 
  фрагмент книги: <br>
  <br>
  «…Мы видели его [С. Разина] на шлюпке, раскрашенной и отчасти покрытой позолотой, 
  пирующего с некоторыми из своих подчиненных. Подле него была дочь одного персидского 
  хана, которую он с братом похитил из родительского дома во время своих набегов 
  на Кавказ. Распаленный вином, он сел на край шлюпки и, задумчиво поглядев на 
  реку, вдруг вскрикнул: «Волга славная! Ты доставила мне золото, серебро и разные 
  драгоценности, ты меня взлелеяла и вскормила, ты начало моего счастья и славы, 
  а я неблагодарный ничем еще не воздал тебе. Прими же теперь достойную тебе жертву!» 
  С сим словом схватил он несчастную персиянку, которой все преступление состояло 
  в том, что она покорилась буйным желаниям разбойника, и бросил ее в волны. Впрочем, 
  Стенька приходил в подобные исступления только после пиров, когда вино затемняло 
  в нем рассудок и воспламеняло страсти. Вообще он соблюдал порядок в своей шайке 
  и строго наказывал прелюбодеяние». (см.: Виктор и Григорий Смолицкие. История 
  одного песенного сюжета // «Народное творчество», №6, 2003). <br>
  <br>
  Однако никакими документами и никакими персидскими источниками не подтверждаются ни утопление, ни вообще похищение Разиным знатной персидской девушки в ходе набега, то есть рассказ Стрейса основан на фольклорном материале, и реальный Разин княжны не похищал и не топил.<br><br>
  Эпизод, опубликованный Корниловичем, был использован Пушкиным в одной из его &quot;Песен о Стеньке 
  Разине&quot; - &quot;Как по Волге-реке, по широкой...&quot; (1826, опубликована 
  в 1881); но распространения она не получила. Популярные ныне песни о Разине 
  были сложены позднее, поэтами эпохи народничества, в 1860-е-1880-е гг. Большую 
  роль в этом сыграла монография Н. И. Костомарова "Бунт Стеньки Разина", написанная 
  в 1858 году (см., например, песню Навроцкого <a href="estnavolge.htm">&quot;Есть 
  на Волге утес&quot;</a>, &lt;1870&gt;). Возможно, что публикация в 1881 году 
  разинских песен Пушкина также оказала влияние на Садовникова. <br>
  <br><br>
  <strong>ПЕСНЯ У ТУРИСТОВ</strong><br>
  <br>
  Известен туристический вариант исполнения, появившийся в последние десятилетия 
  XX века. При этом после каждого куплета кто-то один спрашивает: «Девушки, где 
  вы?». Остальные хором отвечают: «Тута, тута». Первый: «А моей Марфуты нету тута?». 
  Все хором: «А твоя Марфута сигает с парашютом». И после этого все хором затягивают 
  солдатскую:<br>
  <br>
  Соловей, соловей, пташечка –<br>
  Канареечка жалобно поет.<br>
  Раз-поет, два-поет, три-поет,<br>
  Канареечка жалобно поет.<br>
  (Вариант: Помирает – петь не перестает) <br>
  <br>
  Далее – следующий куплет («На переднем Стенька Разин…»). Предполагается петь 
  ее до конца с веселыми припевами после каждого четверостишия. Но практически 
  – все кончается на двух-трех первых куплетах, и до бедной княжны так и не доходит. 
  (см.: Виктор и Григорий Смолицкие, там же). <br>
