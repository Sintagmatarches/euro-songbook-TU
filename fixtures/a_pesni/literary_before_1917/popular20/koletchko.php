1. Протеряла я колечко
</span><br>
<br>Потеряла я колечко, <br>
Потеряла я любовь, <br>

Как по этому колечку <br>
Буду плакать день и ночь.<br>


Где ж девался тот цветочек, <br>
Что долину украшал, <br>

Где мой миленький дружочек, <br>

Что словами улещал? <br>

Улестил милый словами, <br>
Сам уехал навсегда, <br>

Мил уехал, мил оставил <br>
Мне малютку на руках.<br>

Гляну, гляну на малютку:<br>
Словно милый был такой;<br>
Гляну, гляну на малютку, - <br>
Я слезами обольюсь.<br>

Чрез тебя, моя малютка, <br>
Пойду в море утоплюсь.<br>

Долго русою косою <br>
Трепетала по волнам, <br> 
Правой рученькой махала:<br>
&quot;Прощай, милый мой, прощай!&quot;<br>
Ни на что так не взирала, <br>
Как на мил<strong>о</strong>го белый дом, <br>
Ни об ком так не страдала, <br>
Как о милом об своем. <br>
