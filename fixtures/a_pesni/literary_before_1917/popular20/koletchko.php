Народная песня, по одной версии, восходящая к стихотворению 
поэта-самоучки Матвея Ожегова &quot;Колечко&quot; &lt;1896&gt;, а по другой - существовавшая ранее стихотворения  и послужившая его основой. В годы Второй мировой войны ее заглавие 
было использовано для  советского плаката, посвященного победе под 
Сталинградом (художники Кукрыниксы, плакат <a href="../ww2/placat/koletchko.htm">&quot;Потеряла 
я колечко...&quot;</a>).<br>
