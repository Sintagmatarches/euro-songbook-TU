<span class='bold'> СТИХОТВОРЕНИЕ ОЖЕГОВА<br>
<br>
Колечко</span><br>
<br>
Матвей Ожегов<br>
<br>
Сокрушилося сердечко,<br>
Взволновалась в сердце кровь,<br>
Потеряла я колечко,<br>
Потеряла я любовь.<br>
<br>
Я по этом по колечке<br>
Буду плакать-горевать,<br>
По любезном по дружочке<br>
Поневоле тосковать.<br>
<br>
Я по бережку гуляла,<br>
По долинушке прошла;<br>
Там цветочек я искала,<br>
Но цветочек не нашла.<br>
<br>
Много цветиков пригожих,<br>
Да цветочки всё не те –<br>
Всё цветочки непохожи<br>
По заветной красоте.<br>
<br>
Непохожи на те очи,<br>
Что любовью говорят,<br>
Что, как звезды полуночи,<br>
Ясно блещут и горят.<br>
<br>
Где девался тот цветочек,<br>
Что долину украшал,<br>
Где мой миленький дружочек,<br>
Что словами обольщал?<br>
<br>
Обольщал милый словами,.<br>
Уговаривал всегда:<br>
Не плачь, девица, слезами,<br>
Не покину никогда.<br>
<br>
Мил уехал и оставил<br>
Мне малютку на руках,<br>
Обесчестил, обесславил, -<br>
Жить заставил в сиротах.<br>
<br>
Как взгляну я на сыночка,<br>
Вся слезами обольюсь,<br>
Пойду с горя к быстрой речке<br>
Я, сиротка, утоплюсь.<br>
<br>
Нет, уж бог один свидетель,<br>
Полагаюсь на него,<br>
Мне не жаль тебя, мучитель,<br>
Жаль малютку твоего.<br>
<br>
&lt;1896&gt;<br>
