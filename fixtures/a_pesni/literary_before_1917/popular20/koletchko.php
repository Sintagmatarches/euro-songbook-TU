<strong>Матвей Иванович Ожегов </strong>(6 августа 1860, д. Михино Нолинского уезда Вятской губернии - 19 февраля 1934,  г. Перов Московской области)

- поэт-самоучка. Учился в Колобовской сельской земской школе, работал в Перми и в Сибири. С 1885 года - в Москве. Участник суриковского литературно-музыкального кружка. Выпустил стихотворные сборники «Песни и стихотворения» (1891) и «Поля и деревня» (1892). В книге «Моя жизнь и песни для народа» (1-3 книги, М., 1901) опубликовал свою автобиографию.<br>

