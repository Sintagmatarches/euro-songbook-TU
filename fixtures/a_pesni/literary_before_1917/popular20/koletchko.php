	<h1>ПОТЕРЯЛА 
Я КОЛЕЧКО </h1>
<br>
Потеряла я колечко, <br>
Потеряла я любовь, <br>
Да любовь, наверно, <br>
Потеряла я любовь. <br>
<br>
Как по этому колечку <br>
Буду плакать день и ночь, <br>
День и ночь, наверно, <br>
Буду плакать день и ночь. <br>
<br>
Где девался тот цветочек, <br>
Что долины украшал, <br>
Украшал, наверно, <br>
Что долины украшал? <br>
<br>
Где тот миленький дружочек, <br>
Что словами улещал. <br>
Улещал, наверно, <br>
Что словами улещал? <br>
<br>
Улестил меня словами, <br>
Сам уехал далеко, <br>
Далеко, наверно, <br>
Сам уехал далеко.<br>
<br>
Сам уехал, меня бросил <br>
И малютку на руках, <br>
На руках, наверно, <br>
И малютку на руках.<br>
<br>
Как взгляну я на малютку, <br>
Слезой горькой обольюсь, <br>
Обольюсь, наверно, <br>
Слезой горькой обольюсь.<br>
<br>
Чрез тебя ли, мой дружочек, <br>
Пойду в речку утоплюсь, <br>
Утоплюсь, наверно, <br>
Пойду в речку утоплюсь. <br>
<br>
Долго русою косою <br>
Колыхалась по волнам, <br>
По волнам, наверно, <br>
Колыхалась по волнам. <br>
<br>
Правой рученькой махала, <br>
Прощай, милый мой, прощай,<br>
Милый мой, наверно, <br>
Прощай, милый мой, прощай! <br>
