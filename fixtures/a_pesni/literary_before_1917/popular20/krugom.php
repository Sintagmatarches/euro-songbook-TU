<h1>КРУГОМ-КРУГОМ 
	НЕ СВЕТИТСЯ</h1> 
	
	<br>
	Кругом-кругом не светится,<br>
  Кругом ночная тьма,<br>
  Весь народ спокойно спит,<br>
  А я не сплю одна.<br>
  А я не сплю, все думаю,<br>
  Как на свете жить,<br>
  Еще я раз подумаю,<br>
  Как милого забыть.<br>
  Забыть его с надеждою,<br>
  Я это все смогу,<br>
  А вспомню любовь прежнюю –<br>
  Я все его люблю.<br>
  Ветер занавесочку <br>
  Тихонько шевелит,<br>
  Мой милый под окошечком<br>
  С другою говорит.<br>
  Заходит он в комнату,<br>
  Всем руку подает,<br>
  А я сижу грустна, смутна,<br>
  Ко мне не подойдет.<br>
  Заходит он в комнату,<br>
  Накручивает усы,<br>
  Снимает он фуражечку,<br>
  Сам смотрит на часы.<br>
  - Скажи, скажи мне, миленький,<br>
  Который уже час,<br>
  Еще скажи мне, миленький,<br>
  Когда разлука у нас.<br>
  - Не плачь, не плачь, девчоночка,<br>
  Пока еще люблю,<br>
  Успеешь ты наплакаться,<br>
  Как замуж не возьму.<br>
<br>
	<span class='i'>Записана от 
  Понахно Е.П., 1913 г. р., пос. Фабричный, в 1977 г. <br>
  <br>
  Багизбаева М.М. Фольклор семиреченских казаков. Часть 2. Алма-Ата: Мектеп, 
  1979. № 56</span><br>
