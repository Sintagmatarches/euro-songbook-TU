  Кругом, кругом огни горят...<br></span>
  <br>
  Кругом, кругом огни горят,<br>
 Кругом ночная тьма,<br>
Весь народ спокойно спит,<br>
Не сплю я одна.<br>
<br>
Не сплю я и всё думаю,<br>
Как мне на свете жить,<br>
Ещё я раз подумаю,<br>
Как милого забыть.<br>
<br>
Забыть его с надеждою -<br>
Всё это я смогу,<br>
Но как вспомню любовь прежнюю,<br>
Опять его люблю.<br>
<br>
А ветер занавесочки <br>
Тихонько шелестит.<br>
А милый под окошечком<br>
С другою говорит.<br>
<br>
Заходит милый в комнату,<br>
Всем руку подаёт,<br>
А я сижу грустна-грустна,<br>
Ко мне не подойдёт.<br>
<br>
И ходит мил по комнате,<br>
Закручивает усы.<br>
Снимает он фуражечку,<br>
Сам смотрит на часы.<br>
<br>
- Скажи, скажи, мой миленький,<br>
Скажи, который час.<br>
Ещё скажи, мой миленький,<br>
Когда разлука у нас.<br>
<br>
- Разлука неизвестная,<br>
Пока ещё люблю.<br>
Управишься, наплачешься,<br> 
Когда совсем уйду.
<br> <br>
  И вот она, кареточка,<br> 
  И вот он, бурый конь.<br>
  И сел мой разлюбезный,<br> 
  Тем кончилась любовь.<br>
  <br>
