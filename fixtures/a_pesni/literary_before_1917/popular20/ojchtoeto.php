<h1>ОЙ, ЧТО ЭТО ЗА САДОЧЕК... </h1> 
  <br>
Ой, что это за садочек,<br>  
Сад зелёненький такой.<br>
Ой, что ж это за мальчишка<br>  
Развесёленький такой.<br>
<br>  
При народе, при компаньи<br>
Парень девушку обнял.<br>  
Он обнял её в объятья,<br>
Целовать её начал.<br>  <br>
А девчонке стыдно стало,<br>  
Стала плакать и рыдать.<br>
А мальчишке жалко стало,<br>  
Стал девчонку унимать.<br>
<br>
Вынул розвенький платочек,<br>
Стал ей слёзы утирать.<br>
- Не плачь, девка, не плачь, красна,<br>
Возьму замуж за себя.<br>
<br>
- Мой папаша ведь богатый,<br>
Не отдаст он за тебя.<br>
- Не хвались, девка, бьгатством,<br>
Хвались, девка, красотой.<br>
<br>
