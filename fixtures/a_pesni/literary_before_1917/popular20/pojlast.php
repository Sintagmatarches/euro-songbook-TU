	<span class="header"><strong>ПОЙ, ЛАСТОЧКА, ПОЙ!</strong><br>
	</span><em>(Модный романс)<br>
    </em><br>
Ветерок чуть колышет листочки,<br>
Где-то пьяная песня слышна.<br>
Ведь недавно еще ты в платочке<br>
Здесь по саду гуляла одна!....<br>
А теперь ты в шелки разодета,<br>
Без теснения себя продаешь...<br>
Все, что было святого, забыто!<br>
На эстраде ты песни поешь...<br>
<br>
Пой, ласточка, пой!<br>
Но что будет с тобой?<br>
Молодость сгинет твоя навсегда,<br>
Что же будет тогда?<br>
<br>
Его жизнь — это море страданий;<br>
Он не крал, он был честен, всегда,<br>
И свидетельских, ряд, показаний<br>
Подтвердит то вам, господа.<br>
Так в суде адвокат не из важных<br>
Говорил, говорил часов шесть:<br>
Но на лицах у всех у присяжных<br>
Можно было давно уж прочесть:<br>
<br>
Пой, ласточка, пой!<br>
Это долг ведь твой.<br>
Но твой страдалец, решили уж мы,<br>
Не минует тюрьмы!<br>
<br>
Шепчет франтик певице: клянуся,<br>
Всей душою люблю я тебя...<br>
Согласись только милая дуся...<br>
Все что спросишь, отдам я любя...<br>
Брось ты эти подмостки театра,<br>
Здесь ты нравственно падаешь в грязь.<br>
Красоту наводя Клеопатра<br>
Отвечала жуиру, смеясь:<br>
<br>
Пой, ласточка, пой<br>
Только знай друг мой,<br>
Я изучила всех вас<br>
Ничего, кроме фраз...<br>
