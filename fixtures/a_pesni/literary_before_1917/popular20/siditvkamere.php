  <br>
  1. В сыром подземном каземате</strong><br>
  <br>
  В сыром подземном каземате,<br>
  Прикован к каменной стене,<br>
  Влачит лихие дни убивец, <br>
  Проклятый богом и людьми. <br>
  <br>
  &quot;А ты скажи, скажи, детина, <br>
  Ох, сколько душ ты загубил?&quot; <br>
  &quot;Я девятнадцать душ крещенных <br>
  И двесте двадцать три жида. <br>
  <br>
  Беру гитару тонку, звонку, <br>
  Беру я финское перо, <br>
  Иду на дело быстро, смело, <br>
  Убить, зарезать - хоть бы что. <br>
  <br>
  К купцу под утро я забрался, <br>
  Сломал я множество замков, <br>
  Одну карымочку зарезал <br>
  И вот громила - был таков. <br>
  <br>
  Я куш сорвал на семьсот тысяч, <br>
  Купил огромный новый дом <br>
  И рысаков орловских пару, <br>
  И шансонеток полон двор. <br>
  <br>
  На рысаках я разъезжался <br>
  По городам, по деревням, <br>
  Домой я поздно возвращался, <br>
  Где начиналася игра. <br>
  <br>
  Кто приходил ко мне с деньгами,<br>
  Тот уходил всегда пустой,<br>
  А я над ними все смеялся <br>
  С своей красавицей-женой&quot;.<br>
  <em><br>
Песни узников. Составитель Владимир Пентюхов. Красноярк: Производственно-издательский 
  комбинат &quot;ОФСЕТ&quot;, 1995. </em><br>
