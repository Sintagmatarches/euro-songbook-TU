	<span class="header"><strong>СИДИТ В КАМЕРЕ МАЛЬЧИШКА </strong></span><br>
	<br>
  Во подземном каземате, <br>
  Проклят Богом и людьми, <br>
  Сидит в камере мальчишка – <br>
  Лет шашнадцати дитё. <br>
  <br>
  — Ты скажи, скажи, мальчишка, <br>
  Сколько душ ты загубил? <br>
  — Восемнадцать православных <br>
  И сто двадцать три жида. <br>
  <br>
  Брал я звонкую гитару, <br>
  Брал я финское перо,<br>
  Шёл на дело быстро, смело, <br>
  Бить ли, резать — всё равно. <br>
  <br>
  - За жидов тебя прощаем, <br>
  А за русских — никогда! <br>
  Завтра утром на рассвете <br>
  Расстреляем мы тебя.<br>
  <br>
  <em>Старая каторжанская песня, известна ещё до революции. В ней отражён «народный 
  антисемитизм», свойственный некоторым произведениям русского фольклора. Например, 
  в апокрифе «Хождение Богородицы по мукам» Дева Мария спускается в ад и произносит 
  слова в защиту каждой категории грешников; но, когда видит мучающихся в кипящей 
  смоле евреев, требует от чертей подбавить жару, поскольку именно иудеи распяли 
  Её Сына. <br>
   <br>
Жиганец Ф. Блатная лирика. Сборник. Ростов-на-Дону: «Феникс», 
  2001, с. 16.</em><br>
