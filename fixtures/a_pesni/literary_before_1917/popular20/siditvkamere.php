	<strong>2. Жил я в городе Одессе...</strong><br>
	<br>Жил я в городе Одессе<br>
	  Да на улице Семской,<br>
	  Там стоят четыре башни,<br>
	  Посредине дом большой.<br>
	  Тот не дом и не больница,<br>
	  А настоящая тюрьма:<br>
	  В той тюрьме сидит мальчишка<br>
	  Лет двенадцати дитя.<br>
	  Он не слышит и не видит,<br>
	  Когда ключники идут,<br>
	  Отворяют тихо двери<br>
	  И такую речь ведут:<br>
	  «Ты признайсь, признайсь, мальчишка,<br>
	  Сколько душ ты загубил?» -<br>
	  «Восемнадцать православных<br>
	  И сто двадцать пять других». -<br>
	  «За других тебя прощаем,<br>
	  А за русских никогда».<br>
	  Выйди, выйди ты, мальчишка!<br>
	  Посмотри на белый свет<br>
	  Сколько там стоит народу,<br>
	  А посредине мать, отец».<br>
	  Не плачь, папа, не плачь, мама,<br>
	  Не плачь, родная сестра.<br>
	  Завтра рано на рассвете<br>
	  На свободу выйду я».<br>
