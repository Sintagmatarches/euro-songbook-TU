Сухой я корочкой питалась,<br>
Холодную воду я пила.<br>
Тобой бы я, милый, наслаждалась,<br>
И тем довольна я была.<br>
<br>
Сними мне комнату сырую<br>
И я буду одна в ней жить.<br>
Прии ты ко мне, милый, раз в неделю,<br>
И всё равно буду любить.<br>
<br>
И сколько я писем не писала,<br>
И ты, мой милый, не читал.<br>
И сколько я плакала, рыдала,<br>
И ты, мой милый, не слыхал.<br>
<br>
И сколько я леса не рубила,<br>
И крепче дуба не нашла.<br>
И сколько я милых не любила,<br>
Милей Ванюши не нашла.<br>
