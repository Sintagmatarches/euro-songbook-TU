<h1>КОРОЧКА </h1> 
  <br>
  Сухой бы я корочкой питалась,<br>
Воду холодную пила,<br>
Одним бы тобою любовалась,<br>
И тем я счастлива была.<br>
<br>
Сними ты мне комнату сырую,<br>
Чтоб могла я в ней прожить,<br>
Найди себе ты милую такую,<br>
Чтоб могла тебя любить.<br>
<br>
Сколько я писем не писала,<br>
Им сказал, что не читал, не читал,<br>
Сколько ты слез не проливал,<br>
Им сказал, что не видал, что не видал.<br>
<br>
Сними ты мне комнату сырую,<br>
Два с полтиной заплати, заплати.<br>
Найди себе ты милую такую<br>
Чтобы могла с тобой уйти.<br>
<br>
Сколько я лесу не рубила<br>
Крепче дуба, да не нашла<br>
Ах, сколько я милых не любила,<br>
Лучше Юры не нашла.<br>
<br>
Сними ты мне комнату сырую,<br>
Чтоб могла я в ней прожить,<br>
Найди себе ты милую такую,<br>
Чтоб могла тебя любить. <br>
