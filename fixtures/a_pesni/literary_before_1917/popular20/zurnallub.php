  <strong>2. В понедельник я влюбился</strong><br>
 <br>
В понедельник я влюбился,<br>
Весь я вторник прострадал,<br>
В среду я в любви открылся,<br>
А в четверг решенье взял.<br>
<br>
В пятницу пришло решенье, <br>
А в субботу под венец.<br>
В воскресенье обвенчался –<br>
И любви моей конец.<br>
<br>
В понедельник сын родился,<br>
А во вторник Бог прибрал.<br>
В среду я с женой подрался,<br>
А в четверг ее прогнал.<br>
<br>
<em>В нашу гавань заходили корабли. Вып. 3. М., Стрекоза, 2000.</em><br>
