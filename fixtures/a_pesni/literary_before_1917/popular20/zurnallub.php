  И весь вторник я страдал, <br>
  В любови в середу открылся, <br>
  А в четверг ответа ждал. <br>
  Пришло в пятницу решенье, <br>
  Чтоб не ждал я утешенья. <br>
  В скорби, грусти и досаде<br>
  Всю субботу размышлял <br>
  И не зря путей к отраде, <br>
  Жизнь покончить предпринял.<br>
  Но, храня души спасенье, <br>
  Я раздумал в воскресенье. <br>
  В понедельник же другой <br>
  Получил я от драгой <br>
  Ответ нежный и приятный <br>
  И с желанием согласный. <br>
  А во вторник отписал <br>
  И письмо я к ней послал, <br>
  В коем все то выражал <br>
  И всю страсть ей объяснял. <br>
  В среду думал сам себе, <br>
  Как придет она ко мне,<br>
  Сколько радости мне будет.<br>
  Скажу точно - не забудет. <br>
  В четверток моя любезна <br>
  Отписала мне полезно, <br>
  Я в пяток, чтоб вечерка <br>
  Ожидал ее у двора. <br>
  С нетерпеньем ждал часа, <br>
  Как пришла ко мне краса. <br>
