<strong>3. В понедельник я влюбился</strong><br>
<br>
В понедельник я влюбился,<br>
Весь я вторник прострадал,<br>
В среду я в любви открылся,<br>
А в четверг письмо послал,<br>

В пятницу пришло решенье - <br> 
Знать, судьбы моей конец,
<br> 
Во субботу обрученье,<br>
В воскресенье под венец.<br>

В понедельник сын родился,<br>
А во вторник бог прибрал,<br>
В среду с женкой поругался,<br>
А в четверг ее прогнал,<br>
В пятницу пошли судиться, <br>
А в субботу обвинять, <br>
В воскресенье присудили: <br>
В понедельник расстрелять.<br>
