    Точный перевод стихотворения &quot;Der &quot;Warjag&quot; австрийского (тирольского) 
    поэта и драматурга Рудольфа Грейнца (1866-1942), впервые опубликованного в мюнхенском 
    журнале &quot;Jugend&quot; 25 февраля 1904 г., через три недели после гибели 
    крейсера (текст см. в конце страницы). Перевод Студенской вышел в апреле 1904 
    г. в санкт-петербургском «Новом журнале литературы, искусства и науки», в 
    том же номере был приведен и оригинал Грейнца. Евгения Михайловна Студенская 
    (в девичестве Шершевская, 1874-1906) - поэтесса и переводчица, выпускница  историко-филологического факультета 
    Санкт-Петербургского университета.<br>
    <br>
  Музыку написал  воспитанник 12-го Астраханского гренадерского полка Алексей Сергеевич Турищев (1888-1962). В 1905 г. он победил с песней	в конкурсе на лучшую песню-марш для торжественного приема, который устраивал  Николай II в честь экипажей «Варяга» и «Корейца». В 1910 г. Турищев окончил Московскую консерваторию по классу кларнета, прошел Первую мировую и Гражданскую войну, в которой воевал за красных, и дожил до хрущевской &quot;оттепели&quot;.<br>
