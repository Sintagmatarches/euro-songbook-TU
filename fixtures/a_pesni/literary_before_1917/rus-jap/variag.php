  По другой версии, используемый настоящее время мотив - результат взаимодействия, по крайней мере, четырех 
      мелодий: А. Б. Виленского (его мелодекламация опубликована в марте 1904), И.Н. Яковлева, И.М. Корносевича и А.С. Турищева.<br>
    <br>
    Песня была популярна до революции 1917 г., вновь обрела популярность во время Великой 
    Отечественной войны. Текст за годы бытования в народе изменился незначительно 
    - исчез лишь куплет про &quot;желтолицых чертей&quot;, но сейчас иногда его 
      снова поют. Замалчивание авторства Грейнца началось, вероятнее всего, с началом 
      Первой мировой войны и ростом в связи с этим антинемецких настроений в обществе. 
      В современной песенной практике песня обычно назывется просто <strong>&quot;Варяг&quot;</strong>: 
      так она озаглавлена и в популярных песенниках, и в аудиоальбомах конца 20-го 
      века (см., например, CD &quot;Митьковские песни. Материалы к альбому&quot;, 
      1996). А песня, которая в раньше называлась &quot;Варяг&quot; или &quot;Гибель 
  &quot;Варяга&quot;, теперь чаще именуется по первым строчкам: <a href="pleschut.php">&quot;Плещут 
      холодные волны...&quot;</a>. <br>
    <br>
    В русской традиции песня песня получила совсем 
    не тот смысл, который вкладывал в свое стихотворение Грейнц. Сохранив формальную 
    точность, Студенская перевела текст патетически, Грейнц же описывает ситуацию 
    подчеркнуто абсурдно и гротескно: тупо идущие на смерть люди ради пустых абстракций, 
    бессмысленная гибель, официально трактуемая как героизм. И Грейнц, и в целом 
    журнал &quot;Jugend&quot; стояли на четких антимилитаристских позициях, и само 
      стихотворение было опубликовано в окружении антимилитаристских и антиимперских 
      статей и карикатур. Это был  антимилитаристский стихотворный памфлет, 
      по иронии судьбы ставший в России боевой песней.<br>
    <br>
    Подробнее о песне, Рудольфе Грейнце, а также построчный перевод его стихотворения 
    см. В. Гуркович. <a href="a-warjag.php">Тайны создания русской народной песни 
      «Der «Warjag»</a> // Журнал &quot;Историческое наследие Крыма&quot;, №3-4, 2004.<br>
