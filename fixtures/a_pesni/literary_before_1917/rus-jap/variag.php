    <span class='bold'>Бой у Чемульпо 9 февраля 1904 г.</span><br>
    <br>
    9 февраля (27 января ст. ст.) 1904 года японский флот атаковал русскую эскадру, 
    стоявшую на рейде Порт-Артура, что явилось началом русско-японской войны 1904-1905 
    гг. В тот же день русский крейсер &quot;Варяг&quot; и канонерская лодка &quot;Кореец&quot; 
    оказались блокированными японской эскадрой (6 крейсеров, 8 миноносцев, 3 транспорта) 
    в корейском порту Чемульпо. Русские попытались прорваться, но безуспешно. Бой 
    длился около часа, крейсер получил 8 прямых попаданий, после чего &quot;Варяг&quot; 
    и &quot;Кореец&quot; вернулись на рейд, где &quot;Кореец&quot; был взорван, 
      а &quot;Варяг&quot; в панике затоплен и брошен (экипаж даже не вынес мертвые 
      тела товарищей). Крейсер был поднят и до 1916 года входил в японский флот, после 
      чего был выкуплен Россией. Японская эскадра в бою не понесла никаких потерь, 
      ни один снаряд с русских кораблей не попал в цель, что подтверждается и русскими, 
      и японскими отчетами о сражении. &quot;Варяг&quot; на Тихоокеанском флоте считался 
      худшим кораблем по меткости стрельбы. <br>
    <br>
    Тем не менее, песни живут по собственным законам: ведь их слагали не столько 
    про реальный &quot;Варяг&quot;, сколько про некий идеальный корабль, 
      который погибает, но не спускает флага. <br>
