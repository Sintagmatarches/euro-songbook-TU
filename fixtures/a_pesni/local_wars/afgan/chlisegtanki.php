	<h1>ШЛИ 
СЕГОДНЯ ТАНКИ БЕЗ ПРИВАЛА…</h1>
<br>
Шли сегодня танки без привала, <br>
С грохотом осколков по броне. <br>
Здесь учений нет и радуг мало, <br>
В этой необъявленной войне.<br>
<br>
Жаркая, нерусская погода <br>
Оседает пылью на броне, <br>
Оседает вот уже два года <br>
На афганской этой стороне. <br>
<br>
Дороги секунды здесь, ребята, <br>
Не учебный в поле танкодром, <br>
За ошибки платим не караты, <br>
Цену жизни кровью познаем. <br>
<br>
Пронесется пыль в Афганистане, <br>
Вихрем чьи-то жизни прихватив, <br>
Пусть им вечным памятником станет <br>
Этой песни простенький мотив. <br>
<br>
Ждут своих ребят девчонки где-то,<br>
Фотоснимки бережно храня, <br>
Впору нам встречать любви рассветы, <br>
А не пули в полосе огня. <br>
<br>
Жизнь такая наша безвозвратна, <br>
Не вернуть прожитых мирных дней, <br>
Умирать нам вроде рановато, <br>
А приказ не выполнить страшней. <br>
Умирать нам вроде рановато, <br>
