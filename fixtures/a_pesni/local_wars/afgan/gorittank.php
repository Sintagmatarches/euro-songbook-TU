	<h1>ГОРИТ 
МОЙ ТАНК... </h1>
<br>
Горит мой танк. Горю в нём я. <br>
А за спиной — Отчизна милая моя, <br>
Бескрайняя, заброшена, непахана... <br>
<br>
Горит чечен, рванув мой танк. <br>
А за спиной — клочок земли — <br>
Отчизна тощая его, <br>
Российскими снарядами распахана…<br>
<br>
Горит мой танк. Горю в нём я. <br>
А за спиной — Отчизна милая моя, <br>
Бескрайняя, заброшена, несеяна... <br>
<br>
Горит чечен, рванув мой танк, <br>
А за спиной — клочок земли — <br>
Отчизна тощая его, <br>
Российским «градом», телами рваными усеяна... <br>
<br>
О, Боже праведный! Он знал, зачем <br>
Он лёг под танк, прикрыв собой <br>
Отчизну милую свою и маму милую свою, <br>
Горящую, горящую, горящую... <br>
<br>
Но для чего горю здесь я, в дали чужой?! <br>
А за спиной Отчизна корчится моя, <br>
Бескрайняя, голодная... <br>
Непахана, несеяна, разграблена... <br>
<br>
Горит чечен, рванув мой танк. <br>
А за спиной — клочок земли, <br>
Отчизна тощая его, <br>
Российскими эрэсами палимая... <br>
<br>
...Горит мой танк.. <br>
