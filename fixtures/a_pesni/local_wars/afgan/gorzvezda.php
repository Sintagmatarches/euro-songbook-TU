        <br>
        Вероятно, речь идет о штурме дворца афганского президента Амина 27 декабря 
        1979 г. С этого события началась война СССР в Афганистане. Штурм выполнили 
        54 бойца спецподразделений КГБ при поддержке &quot;мусульманского батальона&quot; 
        армейского спецназа (520 человек) и роты десантников (80 человек). Штурм 
        длился 43 минуты, президент Амин был убит, спецназ КГБ и десантники потеряли 
        по пять человек, &quot;мусульманский батальон&quot; - 9. <br>

