        Горит звезда над городом Кабулом,<br>
        Горит звезда печальная моя.<br>
        Как я хотел, чтоб Родина вздохнула,<br>
        Когда на снег упал в атаке я!<br>
        <br>
        Я лежу, смотрю, как остывает<br>
        Над минаретом синяя звезда.<br>
        Кого-то помнят или забывают…<br>
        О нас и знать не будут никогда.<br>
        <br>
        Без документов, без имен, без наций<br>
        Лежим вокруг зажженного дворца.<br>
        Горит звезда. Пора навек прощаться,<br>
        Разлука тоже будет без конца.<br>
        <br>
        Горит звезда декабрьская, чужая,<br>
        А под звездой дымится кровью снег.<br>
        И я слезой последней провожаю,<br>
        Все, с чем впервые расстаюсь навек.<br>
