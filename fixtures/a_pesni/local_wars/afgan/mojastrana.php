	<h1>МОЯ СТРАНА СОШЛА С УМА</h1>
  <br>
  Андрей Макаревич<br>
        <br>
  
	Рождаясь, не выбирают страну,<br>
	  И нам вовек не оборвать эту нить.<br>
	  Моя страна ушла на войну,<br>
	  И я не смог ее остановить.<br>
<br>Кому — власть да сласть, кому — сума да тюрьма,<br>
	  А я не в силах эту боль превозмочь.<br>
	  Моя страна сошла с ума,<br>
	  И я ничем не могу помочь.<br>
	<br>И что тут делать, и как тут быть,<br>
	  Если все отныне вверх дном?<br>
	  Не надо нимбы и крылья растить,<br>
	  Надо просто не быть говном!<br>
	<br>И я уверен только в одном:<br>
	  Пришла пора выбирать.<br>
	  Но если решил не быть говном -<br>
	  И жить легко, и умирать.<br>
	<p>И жить легко, и умирать.<br>
	  И жить - и не умирать!<br>
