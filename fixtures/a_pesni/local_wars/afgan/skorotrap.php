	<h1>СКОРО 
ТРАП НАМ ПОДАДУТ…</h1>
<br>
Скоро трап нам подадут, <br>
Загудят винты привычно. <br>
Вот и кончился уж срок <br>
Нашей службы заграничной. <br>
Чтобы скрыть свою печаль, <br>
Я смотреть стараюсь прямо. <br>
До свиданья и прощай, <br>
До свиданья и прощай, <br>
Заграничная застава. <br>
<br>
Здесь сдружились мы навек, <br>
Словно братья фронтовые, <br>
Не забыть нам никогда <br>
Эти будни боевые. <br>
Столько видели мы здесь! <br>
На душе вдруг горько стало. <br>
Уходить мне жаль до слез, <br>
Уходить мне жаль до слез, <br>
Заграничная застава. <br>
<br>
Дней тревожных и ночей <br>
Здесь не считаны границы, <br>
Бдить покой страны своей <br>
Нам пришлось в чужой столице. <br>
И пускай седых волос <br>
На висках чуть больше стало, <br>
Уходить мне жаль до слез, <br>
Уходить мне жаль до слез, <br>
Заграничная застава. <br>
<br>
Нет приказа, чтобы жить, <br>
Может, встретить смерть придется, <br>
Другом как ни дорожить - <br>
Все же чья-то кровь прольется. <br>
Утро будет для живых, <br>
Ну а павшим — честь и слава, <br>
Будь мне памятью о них, <br>
Будь мне памятью о них, <br>
Заграничная застава. <br>
<br>
Знать, что где-то есть уют, <br>
Тот, который сердцу дорог, <br>
Лишь бы знать, что где-то ждут, <br>
Как бы ни был путь наш долог. <br>
И пускай седых волос <br>
На висках чуть больше стало, <br>
До свиданья и прощай, <br>
До свиданья и прощай, <br>
Заграничная застава. <br>
<br>
И пускай седых волос <br>
На висках чуть больше стало, <br>
Низко кланяюсь тебе, <br>
Низко кланяюсь тебе, <br>
Заграничная застава. <br>
