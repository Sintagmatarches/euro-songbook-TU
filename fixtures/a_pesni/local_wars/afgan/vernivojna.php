	<h1>ВЕРНИ, ВОЙНА, 
МНЕ ЖЕНИХА… </h1>
<br>
Земля России вся в могилах,<br>
  В надгробных новеньких крестах.<br>
  Кому во благо ждать служивых<br>
  Не со щитом, а на щитах?<br>
  Во славу был штурм Измаила,<br>
  А прежде бранная Казань.<br>
  Не потому ль брались за вилы<br>
  Отцы погибших за Назрань?<br>
<br>
Верни, война, мне жениха,<br>
  Уйди подальше от греха<br>
  С порога русского однажды.<br>
  Здесь похоронка в доме каждом.<br>
  Уйди подальше от греха…<br>
<br>
Царям-правителям России<br>
  Проклятье шлю издалека,<br>
  И наши свадебные платья<br>
  В шкафах которые века.<br>
  Что будет дальше, я не знаю,<br>
  Но вижу, милого зовут<br>
  Присягу дать родному краю,<br>
  А сапоги омыть – на юг.<br>
<br>
Верни, война, мне жениха,<br>
  Уйди подальше от греха<br>
  С порога русского однажды.<br>
  Здесь похоронка в доме каждом.<br>
  Уйди подальше от греха…<br>
<br>
Пришла в деревню похоронка,<br>
  Вмиг поседела его мать.<br>
  А я не знала, как утешить,<br>
  И кого больше проклинать.<br>
  Доколе будут в мире войны?<br>
  За что погиб любимый мой?<br>
  За что я в девках засиделась<br>
  И чем грешна перед судьбой?<br>
<br>
Верни, война, мне жениха,<br>
  Уйди подальше от греха<br>
  С порога русского однажды.<br>
  Здесь похоронка в доме каждом.<br>
  Уйди подальше от греха…<br>
