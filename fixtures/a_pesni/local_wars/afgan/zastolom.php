	<h1>ЗА СТОЛОМ, В РЕСТОРАНЕ…</h1>
<br>
За столом в ресторане<br>
Парень крепкий, седой<br>
Пил стакан за стаканом,<br>
Говоря сам с собой.<br>
<br>
С каждым третьим с бутылкой<br>
Почему-то вставал,<br>
Не отведывал вилкой,<br>
Только чуть запивал.<br>
<br>
На вопрос: - Разрешите?<br>
Он качал головой:<br>
- Я прошу вас, уйдите,<br>
Мне б побыть сам с собой.<br>
<br>
Тихо музыка льется,<br>
Смех и дым сигарет.<br>
Вот уже и не пьется…<br>
До покоя сто лет…<br>
<br>
Рассказать, как он выжил,<br>
Да вот только кому?<br>
Не поймет, кто не трогал<br>
Этой жизни струну.<br>
<br>
- Мне еще принесите! -<br>
Он все пил, не пьянел.<br>
- Ах, ребята, простите,<br>
Что вас нет, а я цел…<br>
<br>
<span class='i'>Сиреневый туман: Песенник / Сост. А. Денисенко. Новосибирск: Мангазея, 2001. (Хорошее настроение).</span><br>
<br>
<br>
Песня от имени участника одной из локальных войн 1970-1990-х гг., который выжил, хотя его сослуживцы погибли.<br>

