	<h1>ЗДРАВСТВУЙ, МАМА </h1>
<br>
Здравствуй, мама, я опять пишу письмо,<br>
  Здравствуй мама, мне как прежде хорошо.<br>
  Солнце светит, все нормально, за горой стоит туман,<br>
  Знаешь, мама, здесь не страшно, просто здесь Афганистан.<br>
<br>
Здравствуй, мама, я во сне всех видел вас.<br>
  Как сестренка перешла в девятый класс?<br>
  Ты бы мама Ольгу в гости к нам почаще бы звала.<br>
  Ты скажи ей, что вернусь я, чтоб она меня ждала.<br>
<br>
Мать читает, а в глазах стоит туман,<br>
  Мать не знает, что здесь правда, что обман.<br>
  Мать не в силах оторваться от могильного холма.<br>
  Сын в земле, а на ладони листик этого письма.<br>
<br>
Лишь вчера осенним ветром дунуло окно,<br>
  А сегодня на рассвете почтальон принес письмо.<br>
  В нем живой он и смеется, и как прежде хочет жить.<br>
  - Здравствуй мама, здравствуй вечность, я не вправе вас забыть…<br>
<br>
