	<h1>ЖУРАВЛИ АФГАНА</h1>
<br>
Здесь под небом чужим, под кабульской лазурью,<br>
Слышу крик журавлей, улетающих вдаль.<br>
Ах, как хочется мне заглянуть в амбразуру,<br>
Пулеметом глушить по России печаль!<br> 
<br>
День и ночь напролет с боевым автоматом,<br>
Как братишка родной, пистолет на ремне.<br>
И хотелось бы мне обложить землю матом -<br>
Слезы горести лить ни к чему на войне.<br>
<br>
Нас навеки судьба боевая связала,<br>
Нам погибших друзей не забыть никогда.<br>
Расплескали мы кровь по Афгану немало<br>
И придется еще, коль возникнет нужда.<br>
<br>
Только просьба ко всем: не забыть эти встречи,<br>
Улетев, не забудь, как вершили дела,<br>
Не забудьте друзей, не забудьте их плечи -<br>
Их поддержка в бою счастье вам принесла.<br>
<br>
<span class='i'>Сиреневый туман: Песенник / Сост. А. Денисенко. Новосибирск: Мангазея, 2001. (Хорошее настроение).</span><br>
