  <strong>2. Ночь темна…</strong><br>
  <br>
  Ночь темна. Лови минуты!<br>
  Но стена тюрьмы крепка,<br>
  У ворот ее замкнуты<br>
  Два железные замка.<br>
  <br>
  «Часовой!» — «Что, барин, надо?» -<br>
  «Притворись, что ты заснул,<br>
  А я вмиг через ограду <br>
  Тенью б быстро промелькнул?<br>
  <br>
  Край родной увидеть надо <br>
  Да жену поцеловать, <br>
  А потом уж в лес зеленый,<br>
  В лес дремучий – там гулять!» <br>
  <br>
  «Рад помочь, куда ни шло бы! <br>
  Божья тварь, чай, то ж и я.<br>
  Пуля, барин, ничего бы,<br>
  Да боюся батожья! <br>
  <br>
  Отдадут под суд военный... <br>
  А сквозь строй как проведут, -<br>
  Только труп окровавленный <br>
  На тележке увезут!» <br>
  <br>
  Шепот смолк... Всё тихо снова.... <br>
  Где-то бог подаст приют? <br>
  То ль схоронят здесь живого? <br>
  То ль на каторгу ушлют?<br>
  <br>
  Будет вечно цепь надета, <br>
  Да начальство станет бить... <br>
  Ни ножа, ни пистолета... <br>
  И не знаешь, как тут быть…<br>
