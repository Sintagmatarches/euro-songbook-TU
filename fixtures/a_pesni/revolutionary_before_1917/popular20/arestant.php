  5-е изд. Э. Л. Каспровича. Лейпциг, 1879. По свидетельству Н.А. Тучковой-Огаревой, написано во время заключения в III 
  Отделении (Тучкова-Огарева Н. А. Воспоминания. [М.], 1959. С. 93). Ст-ние было широко известно как народная и солдатская песня и особенно распространено в Пензенской и Саратовской губерниях (Пругавин А. Песня о часовом и барине // Нижегородский сборник. Спб, 1905. С. 276-282. Первый, ставший известным, вар. этой песни, записанный в Петровске Саратовской губернии, представляет собою значительно измененный текст. Второй вар., более близкий к тексту Огарева, - там же. Известны переработки 1934-го и последующих годов (см.: Андреев Н.П. Русский фольклор: Хрестоматия для высших пед. учебных заведений. 2-е изд. М7, 1938. С. 560). В среде петроградских рабочих в конце XIX или в начале XX в. некоторые строфы были с изменениями авторского текста включены в состав анонимного ст-ния <a href="../starrev/na1izulic.htm">«Литовский 
  замок»</a> (Рукописный отдел Института русской литературы (Пушкинского дома) АН СССР, сектор народного творчества, запись 1937 г.). Ст-ние, кроме того, не раз перекладывалось на музыку; мелодия этой песни использована Д.Д. Шостаковичем в 11 симфонии. Известны переработки на болгарском языке (см. Песни и романсы русских поэтов / Вступ. статья, подготовка текста и примеч. В.Е. Гусева. М.; Л., 1963 (Б-ка поэта, БС)). С. 1032).<br>
