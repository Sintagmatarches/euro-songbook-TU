  Николай Огарев<br>
  <br>
  Ночь темна. Лови минуты!<br>
  Но стена тюрьмы крепка,<br>
  У ворот ее замкнуты<br>
  Два железные замка.<br>
  Чуть дрожит вдоль коридора<br>
  Огонек сторожевой,<br>
  И звенит о шпору шпорой,<br>
  Жить скучая, часовой.<br>
  <br>
  «Часовой!» — «Что, барин, надо?»<br>
  - «Притворись, что ты заснул: <br>
  Мимо б я, да за ограду <br>
  Тенью быстрою мелькнул! <br>
  Край родной повидеть нужно <br>
  Да жену поцеловать, <br>
  И пойду под шелест дружный <br>
  В лес зеленый умирать!..» <br>
  <br>
  - «Рад помочь! Куда ни шло бы! <br>
  Божья тварь, чай, то и я! <br>
  Пуля, барин, ничего бы,<br>
  Да боюся батожья! <br>
  Поседел под шум военный... <br>
  А сквозь полк как проведут, <br>
  Только ком окровавленный <br>
  На тележке увезут!» <br>
  <br>
  Шепот смолк... Всё тихо снова.... <br>
  Где-то бог подаст приют? <br>
  То ль схоронят здесь живого? <br>
  То ль на каторгу ушлют? <br>
  Будет вечно цепь надета, <br>
  Да начальство станет бить... <br>
  Ни ножа! Ни пистолета!.. <br>
  И конца нет, сколько жить!<br>
  <br>
  24 февраля - 20 марта 1850<br>
