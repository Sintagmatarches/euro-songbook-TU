	  <br>
Чуж-Чуженин<br>
<br>I.<br>
	  Вдоль по дорожке,<br>
	  Прямо из резерва<br>
	  К нам кобылушка бредет...<br>
	<br>На походной кухне<br>
	  На обед и ужин<br>
	  Щи нам с кашею везет...<br>
	<br>Прощевайте, нары!<br>
	  Будут кашевары,<br>
	  Кашевары — повара!<br>
	<br>Щи прокипятили.<br>
	  Кашу проварили,<br>
	  Каше-матушке ура!..<br>
	<br>II.<br>
	  Кухни подъезжают...<br>
	  Марш за котелками...<br>
	  Ложки проч из голенищ!<br>
	<br>Тянем из-за пазух<br>
	  По краюшке хлеба -<br>
	  То-то вдоволь поедим...<br>
	<br>Пусть себе стреляет<br>
	  Немец из орудий -<br>
	  Нам гранаты ни по чем...<br>
	<br>Эй, посторонитесь!<br>
	  Эй, не торопитесь!<br>
	  Подходите чередом!..<br>
	<br>III.<br>
	  Вдруг отколь ни взялся,<br>
	  В небе показался<br>
	  Воробей-аэроплан...<br>
	<br>Над походной кухней<br>
	  Так себе и вьется, -<br>
	  Видно, каши захотел!<br>
	<br>Солдатушки наши<br>
	  Сами хочут каши,<br>
	  Каши немцу не дадут...<br>
	<br>Воробей все вьется,<br>
	  Видно, каши хотца,..<br>
	  Ишь ты, дьяволов хомут!<br>
	<br> IV.<br>
	  Только щи открыли,<br>
	  Только приступили,<br>
	  Вдруг к нам бомба с неба рраз!<br>
	<br>Эко горе наше -<br>
	  Прямо в щи да в кашу<br>
	  Бомбой немец угодил!<br>
	<br>День не мясопустный,<br>
	  Стало бомбе вкусно -<br>
	  Разорваться не хотит!<br>
	<br>До еды добралась,<br>
	  Да не разорвалась<br>
	  И застряла в русских щах!..<br>
	<br>V.<br>
	  Наплескала щами,<br>
	  Разлетелась каша,<br>
	  Не хватает нам для всех...<br>
	<br>Погоди ты, немец,<br>
	  Мы за нашу кашу<br>
	  Вам горохом отомстим...<br>
	<br>Пулеметы наши<br>
	  Мы почище каши -<br>
	  Звонкой сталью зарядим...<br>
	<br>За свободу нашу,<br>
	  Да за щи, за кашу<br>
	  Пулеметом угостим!..<br>
