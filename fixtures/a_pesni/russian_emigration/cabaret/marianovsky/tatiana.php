  Кусты сирени и луну в тиши аллей?<br>
  Татьяна, помнишь грезы былые?<br>
  Тебя любил я, не вернуть нам юных дней. <br>
  <br>
  Упали косы душистые, густые,<br>
  Свою головку ты склонила мне на грудь.<br>
