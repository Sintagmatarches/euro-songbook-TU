  Как мне знакомы твои черты.<br>
  Помнишь ли меня, моя Татьяна?<br>
  Мою любовь, наши прежние мечты? <br>
  <br>
  Вижу губ накрашенных страданье,<br>
  В глазах твоих молчанье пустоты.<br>
