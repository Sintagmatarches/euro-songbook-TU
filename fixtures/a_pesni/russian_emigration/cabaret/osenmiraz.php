Слова и музыка А. Суханова<br>
<br>
Пусть скорбно песнь моя звучит,<br>
Пусть прошлое навек собою заглушит.<br>
Ты не могла меня понять,<br>
Как я тебя любил,<br>
Как мне пришлось страдать.<br>
<br>
И юных дней очарованье,<br>
Весна и первое признанье<br>
Прошли, как дивный, странный сон,<br>
Как горестной судьбы<br>
Безжалостный закон.<br>
<br>
Завял наш бедный сад,<br>
Осыпались цветы,<br>
И на душе больной<br>
Так грустно и тоскливо.<br>
Ушла ты навсегда,<br>
Остались лишь мечты,<br>
Но я храню твой образ бережливо.<br>
<br>
Я помню нежный взгляд<br>
Печальных серых глаз,<br>
Знакомый голос твой<br>
Я слышу в отдаленье,<br>
Но это все мираж,<br>
Тебя давно уж нет,<br>
То осени сырой я слышу дуновенье.<br>
