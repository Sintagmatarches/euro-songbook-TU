  Слова и музыка Оскара Строка<br>
  <br>
  1. Был Яша коммивояжером <br>
  И торговал он всяким вздором. <br>
  Рыжий, лукавый, сам из Либавы, <br>
  Всюду возил товар дешевый: <br>
  Третьего сорта папиросы, <br>
  Пятого сорта пылесосы. <br>
  Бедный Яша плохо торговал <br>
  И больше тратил, чем он наживал. <br>
  Край Прибалтийский объезжая, <br>
  Он всем ужасно надоел, <br>
  И, чемоданчик раскрывая, <br>
  Наш Яша очень грустно пел:<br>
  <br>
  <em>Припев:</em><br>
  <br>
  Купите туфли и чулки, <br>
  И помидоры, и бычки, <br>
  И папиросы, и коньяк, <br>
  И пылесосы, и табак, <br>
  Духи и пудру, и сардинки, <br>
  И всевозможные картинки.<br>
  <br>
  2. Но не везло, увы, бедняжке, <br>
  Плохо торговля шла у Яшки: <br>
  Там не желают, тут высылают, <br>
  Все надувают везде его. <br>
  За контрабанду он судился, <br>
  И за решеткой очутился, <br>
  И, свободу получив опять, <br>
  Он решил отсюда удирать. <br>
  И чтоб дела свои поправить, <br>
  Он отправляется в Сиам <br>
  Своим товаром позабавить <br>
  Всех чернокожих голых дам:<br>
  <br>
  <em>Припев.</em><br>
  <br>
  3. Дамы сиамские смотрели, <br>
  Вещи прекрасны в самом деле: <br>
  Шелк, крепдешины и этамины <br>
  Дамскому сердцу всегда близки. <br>
  Только суров закон Сиама, <br>
  И ни одна не может дама <br>
  Одевать ни платьев, ни чулок, <br>
  Все носят только... фиговый листок. <br>
  Про пылесос они не знают <br>
  И не слыхали патефон, <br>
  Обратно Яша уезжает, <br>
  И напевает грустно он:<br>
  <br>
  <em>Припев.</em><br>
