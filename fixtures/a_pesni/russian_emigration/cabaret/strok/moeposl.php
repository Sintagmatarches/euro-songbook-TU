  «Мое последнее танго» (в действительности, одно из первых танго Строка) 
  написано в конце 1931 года в рижской тюрьме, куда композитор угодил за долги 
  после неудачной попытки открытия собственного артистического ресторана «Барберина». 
  Это одна из песен, написанных Оскаром Строком и Марком Марьяновским в начале 
  1930-х в Риге и вошедших в репертуар начинающего вокальную карьеру Петра Лещенко. 
  См. <a href="../marianovsky/barcelona.php">«Барселона»</a>, <a href="../marianovsky/dunia.php">«Блины»</a>, <a href="../marianovsky/vino.php">«Вино 
