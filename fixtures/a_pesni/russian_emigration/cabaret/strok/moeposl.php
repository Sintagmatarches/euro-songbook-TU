  <a href="katia.php">«Катя»</a>, <a href="../marianovsky/marfucha.php">«Марфуша»</a>, 
  <a href="../marianovsky/miranda.php">«Миранда»</a>, <a href="moeposl.php">«Мое 
  последнее танго»</a>, <a href="musenka.php">«Мусенька»</a>, <a href="nepokidajja.php">«Не 
  покидай, я умоляю!»</a>, <a href="sinrapsod.php">«Синяя рапсодия»</a>, <a href="skazpotch.php">«Скажите, 
  почему?»</a>, <a href="spiserdce.php">«Спи, мое бедное сердце»</a>, <a href="../marianovsky/tatiana.php">«Татьяна»</a>. 
  Лещенко также взял в репертуар написанное в 1927-29 гг. танго Строка <a href="tchernglaza.php">«Черные 
  глаза»</a>.<br>
  <br>
  <br>
  <strong>Из книги Дмитрия Драгилёва &quot;Лабиринты русского танго&quot; (Спб., Алетейя, 
  2008):</strong><br>
  <br>
  &quot;Объявлять одно из первых и очень удачных танго последним — шаг в высшей 
  степени опрометчивый. Но каковы референции? Последняя встреча в кафе «Марсель»? 
  Или в притоне «Трех бродяг», о котором пелось у Юрия Милютина? Или последнее 
  танго в аргентинской таверне? <br>
  <br>
  15 сентября 1931 г. Строк открывает собственный дансинг на ул. Бривибас 31. 
  На первом этаже нового дома ген. Сникера, построенного в модном функционалистском 
  стиле неподалеку от церкви Александра Невского. <br>
  <br>
  Открытие дансинга отмечается пышно. Интерьер в стиле арт деко. Задействованы 
  оркестр Оскара Строка, квартет герлс, гавайские певцы, танцоры, Петр Лещенко, 
  ведущая Эльза Буш, сценарист Лери (Владимир Владимирович Клопотовский) — автор 
  многих эстрадных дивертисментов, а также музыкальных обозрений, представлений 
  и т. п., которые ставились, в частности, на сцене Театра Русской драмы. Вытянутый 
  зал неблагоприятно сказывается на эстрадном пении без микрофона. Лещенко успеха 
  не имеет, его голос кажется слабым и невыразительным. Экономический кризис в 
  Латвии и неудачный менеджмент ресторана довершают картину. <br>
  <br>
  Не просуществовав и трех месяцев, дансинг терпит крах. В ночь на субботу 28 
  ноября 1931 г. у Строков на Мариинской 64 обыск. В ту же ночь композитор был 
  арестован. Суд оправдал Строка по 11 пунктам обвинения, но по двум оставшимся 
  пунктам он попадает на 7 месяцев в долговую тюрьму. Супруга композитора распродает 
  имущество. В камере Строк пишет «Мое последнее танго». Через неполный месяц 
  музыкант выпущен под залог и вместе с семьей вторично уезжает в Берлин, город, 
  который уже в 1933 г. он окончательно покидает, перебравшись в Каунас, а затем 
  вернувшись в Ригу. <br>
  <br>
  Перед отъездом Строка в Берлин Лещенко с большим успехом исполняет «Мое последнее 
  танго» в кафе «АТ» в сопровождении автора. <br>
  <br>
  «Мое последнее танго» тематизирует воспоминания о Франции и является своеобразным 
  прощанием с возлюбленной. Адресат точно неизвестен. Роман с Эльзой Буш? Воспоминание 
  о Лени Либман? К тому же Лещенко подвела дикция и несовершенство грамзаписи: 
  о Марселе он поет, об артели, об отеле или борделе, трудно сказать даже после 
  нескольких прослушиваний. Продолжая «французскую линию», по ритмической структуре 
  это «наиболее аргентинское» танго Строка. В его начальной мелодической фразе 
  использована музыкальная символика с «роковой» коннотацией» (т. н. катабазис 
  — нисходящий хроматический ход — и тритон — считающийся неблагозвучным музыкальный 
  интервал). Оба приема отсылают сразу к трем известным произведениям: танго Гаде 
  «Ревность», хабанере Ж. Бизе из оперы «Кармен», и композиции С. Ирадьера XIX 
  в. как первоисточнику всех танго. Обращение к оперному сюжету «Кармен» не случайно: 
  Хозе, протагонист этой оперы, обольщенный и обманутый роковой женщиной, некоторое 
  время проводит в заключении&quot;. <br>
