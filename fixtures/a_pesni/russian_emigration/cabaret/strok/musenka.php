  <br>
  Опубликовано не ранее середины 1935 года в сборнике Строка &quot;Русский джаз&quot;. 
  Другие авторские названия - &quot;O mia cara&quot; и &quot;Песня любви&quot;. 
  Одна из песен, написанных Оскаром Строком и Марком Марьяновским в начале 1930-х 
  в Риге для начинающего песенную карьеру Петра Лещенко. См. <a href="../marianovsky/barcelona.php">«Барселона»</a>, <a href="../marianovsky/dunia.php">«Блины»</a>, 
  <a href="../marianovsky/vino.php">«Вино любви»</a>, <a href="golubglaza.php">«Голубые 
  глаза»</a>, <a href="../marianovsky/kavkaz.php">«Кавказ»</a>, <a href="katia.php">«Катя»</a>, 
  <a href="../marianovsky/marfucha.php">«Марфуша»</a>, <a href="../marianovsky/miranda.php">«Миранда»</a>, 
  <a href="moeposl.php">«Мое последнее танго»</a>, <a href="musenka.php">«Мусенька»</a>, 
  <a href="nepokidajja.php">«Не покидай, я умоляю!»</a>, <a href="sinrapsod.php">«Синяя 
  рапсодия»</a>, <a href="skazpotch.php">«Скажите, почему?»</a>, <a href="spiserdce.php">«Спи, 
  мое бедное сердце»</a>, <a href="../marianovsky/tatiana.php">«Татьяна»</a>. 
  Лещенко также взял в репертуар написанное в 1927-29 гг. танго Строка <a href="tchernglaza.php">«Черные 
  глаза»</a>.<br>
  <br>
  <br>
  Из книги Дмитрия Драгилёва &quot;Лабиринты русского танго&quot; (Спб., Алетейя, 
  2008):<br>
  <br>
  &quot;Посвящено Марии Эдуардовне (по матери — Фаданелли) — сестре жены композитора. 
  Опубликовано в авторском сборнике «Русский джаз. Альбом популярных песен, танго, 
  фокстротов» — издательством Строка, которое с 1930 г. называется «Казанова». 
  В печати альбом появился по возвращении Строка из дальневосточного турне. В 
  Риге Строк отсутствовал всю первую половину 1935 г. Он планировал переселиться 
  в Токио или Харбин, «выписать» туда свою семью. Но отказался от этой затеи. 
  Непосредственно на Дальний Восток Строк отправился пароходом из Неаполя (до 
  Неаполя — поездом, по некоторым данным через Париж). В Ригу вернулся поездом 
  через Москву&quot;. <br>
