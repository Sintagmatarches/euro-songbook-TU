  <br>
  Помнишь весной день золотой,<br>
  Синие волны моря.<br>
  Пел соловей порой ночной,<br>
  Душа полна была тобой.<br>
  Мне душу жег твой нежный взгляд,<br>
  Далеким было горе.<br>
  И я шептал:<br>
  - С тобой вдвоем<br>
  Любовь и счастье мы найдем.<br>
  Мусенька родная!<br>
  Как я люблю тебя.<br>
  Тихо плачет сердце,<br>
  Зовет тебя любя.<br>
  Мечта любви моей,<br>
  Приди скорей!<br>
  Нас манит счастье светлых,<br>
  Чудных дней.<br>
  Девочка, родная!<br>
  Мы будем вместе вновь.<br>
  Слышишь волны моря<br>
  Нам поют про любовь?<br>
