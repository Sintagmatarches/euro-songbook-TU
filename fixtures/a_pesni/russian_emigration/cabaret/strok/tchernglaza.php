  <br>
  &quot;Черные глаза&quot; - первое танго Оскара Строка - написаны в 1928 году. Танго 
  автобиографическое: у Строка, занявшегося в Риге изданием собственного журнала, 
  вспыхнул роман с секретаршей Лени Либман, ради которой он собирался бросить 
  семью - жену Луизу-Анжелу, дочь Веру и сына Евгения, - и перенес издательство 
  журнала в Париж. Журнал развалился, Либман ушла к другому. Как раз в 1928 году 
  в Париже гастролировал основатель жанра танго-песни аргентинец Карлос Гардель. 
  Возможно, Строк его там слышал. Впервые &quot;Черные глаза&quot; вышли в 1929 
  году на пластинке фирмы &quot;His Master's Voice&quot; в исполнении берлинского 
  оркестра Марека Вебера, пел Марек Белоусов. В СССР его первым исполнили 
  Казимир Малахов с джазом Александра Цфасмана (в Москве) и Иван Миловидов с джазом 
  Якова Скоморовского (в Ленинграде). В начале 1930-х &quot;Черные глаза&quot; 
  вошли в репертуар Петра Лещенко.<br>
  <br>
  В 1930-31 гг. Строк написал <a href="otvetnatcern.php">&quot;Ответ на танго 
  &quot;Черные глаза&quot;</a>.<br>
