Слова и музыка Александра Вертинского<br>
<br>
В бананово-лимонном Сингапуре, в бури,<br>
Когда поет и плачет океан<br>
И гонит в ослепительной лазури<br>
Птиц дальний караван…<br>
В бананово-лимонном Сингапуре, в бури,<br>
Когда у вас на сердце тишина,<br>
Вы, брови темно-синие нахмурив,<br>
Тоскуете одна.<br>
<br>
И нежно вспоминая<br>
Иное небо мая,<br>
Слова мои, и ласки, и меня,<br>
Вы плачете, Иветта,<br>
Что наша песня спета,<br>
А сердце не согрето<br>
Без любви огня.<br>
<br>
И, сладко замирая<br>
От криков попугая,<br>
Как дикая магнолия в цвету,<br>
Вы плачете, Иветта,<br>
Что песня недопета,<br>
Что это лето – где-то –<br>
Унеслось в мечту!<br>
<br>
В опаловом и лунном Сингапуре, в бури,<br>
Когда под ветром ломится банан,<br>
Вы грезите всю ночь на желтой шкуре<br>
Под вопли обезьян.<br>
В бананово-лимонном Сингапуре, в бури,<br>
Запястьями и кольцами звеня,<br>
Магнолия тропической лазури,<br>
Вы любите меня.<br>
