<h1>ТЫ УСПОКОЙ МЕНЯ…</h1>
 
  Слова и музыка Александра Вертинского<br>
  <br>
  Ты успокой меня,  cкажи, что это шутка,<br>
  Что ты по-прежнему, по-старому моя!<br>
  Не покидай меня! 
  Мне бесконечно жутко,<br>
  Мне так мучительно, так страшно без тебя!..<br>
