	<br>
	Музыка Григория Теплова<br>
	<br>
	1. Для того ль я в дни разлуки<br>
Здесь страдала без тебя<br>
И на то-ль жестоки муки<br>
Презирала я, любя,<br>
<div class='refren'>Чтоб тобой самим открылся<br>
Рок погибели моей,<br>
Чтоб правдивый слух носился<br>
О неверности твоей?</div>
<br>
2. Вспомни злое разлученье,<br>
Вспомни клятвы ты свои,<br>
Вспомяни мои мучения<br>
И с лова к тебе мои.<br>
<div class='refren'>Вспомяни, как ты, прощаясь,<br>
Мне, жестокий, говорил,<br>
Как притворно ты терзаясь,<br>
Предо мною слезы лил.</div>
<br>
3. Вот уж вся тебе открылась,<br>
Ты мя ввергнул в злу напасть.<br>
Ах, к чему, к чему вселилась<br>
В грудь мою та щетина страсть!<br>
<div class='refren'>Гасни, гасни, огнь безмерной,<br>
Исцелись, скорбна грудь,<br>
Ну, а ты, о лжец неверной,<br>
Ласки все мои забудь.</div>
