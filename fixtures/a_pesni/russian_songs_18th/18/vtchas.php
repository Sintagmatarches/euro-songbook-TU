	<h1>В ЧАС, КОГДА Я НЕСКАЗАННУ</h1>
	<br>
	Музыка Григория Теплова<br>
	<br>
	1. В час, когда я несказанну<br>
Роскошь в сердце ощущал,<br>
Как себя я выше смертных<br>
В сне прелестном почитал,<br>
<div class="refren"В ту дражайшую минуту<br>
День зловредный наступил<br>
И лучем несносна света<br>
Обольщенный взор сокрыл.</div>
Тень драгая удалилась,<br>
Тщетно силился ловить.<br>
Радость вся моя сокрылась,<br>
Я остался слёзы лить.<br>
<br>
2. О мечта! о сон прелестной!<br>
Ты мою умножил страсть,<br>
Награди мои мученья<br>
И оправь мою напасть.<br>
<div class="refren">Как ты мне являл любезну,<br>
Так меня ты ей яви,<br>
Обольсти прекрасны взоры,<br>
Чувство влей в нея любви.</div>
Чтоб она, воспламеняся,<br>
Нежной страсти сей огнём<br>
В сладких грёзах пробудяся,<br>
То-ж ко мне питала днём.<br>
