  <br>
  Волны взрывая, летит "Стерегущий" <br>
  В туче снарядов навстречу врагам, <br>
  И перед смертью с волною бегущей <br>
  Шлет он прощанье родным берегам. <br>
  <br>
  Но берега те родные далеко, <br>
  Им не дождаться коварной волны. <br>
  Желтое море темно и глубоко, <br>
  Солнце зловеще палит с высоты. <br>
  <br>
  Круг замыкает японская стая, <br>
  Пушки все реже бросают ответ. <br>
  Вот и последняя бомбой подбита. <br>
  Бой угасает. Защитников нет. <br>
  <br>
  Лезут японцы. Их жадные взоры <br>
  Ищут нетронутый пулей мундир. <br>
  Возле орудий лежат командоры. <br>
  В рубке убитый лежит командир. <br>
  <br>
  Но, чу! В миноносце послышались стоны - <br>
  Грозные стоны железных болтов. <br>
  Это матросы открыли кингстоны, <br>
  Вьются воронки с обоих бортов. <br>
  <br>
  Вздрогнуло судно, и, плавно качаясь, <br>
  Медленно шел "Стерегущий" ко дну. <br>
  Гордо Андреевский стяг, развеваясь, <br>
  Отдал салют за родную страну. <br>
  <br>
  Кто это были матросы-герои, <br>
  Знает об этом один только Бог. <br>
  Но знают наверное, что было их двое. <br>
  Дух им воинственный в этом помог. <br>
