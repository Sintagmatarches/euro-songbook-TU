  <br>
  В отличие от подвига &quot;Варяга&quot;, успешный бой команды &quot;Стерегущего&quot; 
  не был мистификацией. Миноносцы &quot;Решительный&quot; и &quot;Стерегущий&quot; 
  13 февраля 1904 г. (по старому стилю) дали бой четырем японским кораблям. 
  &quot;Решительный&quot; прорвался, &quot;Стерегущий&quot; не смог и сражался 
  до последнего человека и снаряда. Все командиры погибли. Из всего экипажа в 
  живых осталось только четверо нижних чинов. По легенде, когда японцы взяли разбитый 
  &quot;Стерегущий&quot; на буксир, двое уцелевших матросов открыли кингстоны 
  и затопили миноносец. &quot;Стерегущему&quot; поставлен памятник в Петербурге 
  в Алексеевском парке у Петропавловской крепости.<br>
