Русская версия неаполитанской песни  Родольфо Фальво (Rodolfo Falvo, 1873-1937)
на слова Энцо Фуско (Enzo Fusco, р. 1899) &quot;Dicitencello vuie&quot; (вариант написания: &quot;Dicitencello vuje&quot;, в переводе - &quot;Скажите вы ей&quot;). Неаполитанский оригинал издан в 1930 г. Русская версия появилась около 1936 г., в том же году записана на пластинку Сергеем Лемешевым в сопровождении орекстра Всесоюзного радиокомитета (прослушать запись можно <a href="http://www.russian-records.com/details.php?image_id=13312" target="_blank">здесь</a>). Пластинки выпущены Ногинским и Апрелевским заводами. В 1950 г. появилась английская версия, в 1959-м - финская.<br>
