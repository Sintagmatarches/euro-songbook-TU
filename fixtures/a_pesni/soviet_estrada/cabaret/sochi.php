<span class='bold'>Аста Галла</span> (Ермолаева Анна Дмитриевна, 1899-1981) - драматург, фельетонист. Родилась  в Гатчине под Санкт-Петербургом. Училась в гимназии в Петрограде, работала в ряде  учреждений. С 1926 г. выступала как автор произведений для эстрады  (скетчи, песни, пародии, фельетоны), печаталась в эстрадных сборниках. В числе ее произведений -текст музыкальной комедии «Встреча на  курорте», одноактная пьеса «Метелица», песни <a href="../drugije/diadiavan.htm">«Дядя Ваня»</a>, «Помни обо мне», <a href="najuge.php">«На юге»</a>, <a href="sochi.php">«Сочи»</a>.<br>

