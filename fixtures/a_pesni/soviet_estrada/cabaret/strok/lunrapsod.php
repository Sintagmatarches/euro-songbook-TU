  <br>
  Новая версия слоу-фокса <a href="sinrapsod.php">&quot;Синяя рапсодия&quot;</a>, 
  написанного Оскаром Строком в 1935 году и входившего в репертуар Петра Лещенко. 
  Переделка создана для Леонида Утесова. Наум Лабковский сочинил новый текст, 
  а Строк аранжировал мелодию прежнего фокстрота в танго.<br>
  <br>
  <br>
  <strong>Из книги Дмитрия Драгилёва &quot;Лабиринты русского танго&quot; (Спб., Алетейя, 
  2008) о &quot;Синей рапсодии&quot;:</strong><br>
  <br>
  &quot;По воспоминаниям Евг.О. Строка (сына композитора) — написано в 1936 г. 
  в поезде для небольшого ревю, которое шло в одном из городов. Указанной дате 
  противоречит тот факт, что ноты и пластинки с данным произведением появляются 
  в продаже уже в 1935 г. Вероятее всего, появлением этого танго (задуманного 
  как слоу-фокс) мы обязаны гастрольной поездке композитора по странам Дальнего 
  Востока, где, по его собственному свидетельству, он участвовал в постановках 
  ревю. Первоначальное название отсылает к «Рапсодии в стиле блюз (в голубых тонах)» 
  Дж. Гершвина и возможно явилось отголоском интереса к дебютам джаз-оркестров 
  О. Лундстрема и Б. Райского в Шанхае и Харбине. Исполнение западной легкой музыки 
  не было сильной стороной оркестров, сопровождавших Петра Лещенко, и медленный 
  фокстрот «Синяя рапсодия», а именно так он обозначен в авторском издании, звучит 
  у Лещенко слишком неопределенно. Новый текст принадлежал перу Наума Лабковского 
  (1944—45 гг. написан для Л.О. Утесова, Строк в это время живет в Москве). Лабковский, 
  поэт-песенник и журналист, сотрудничавший с оркестрами А. Цфасмана, Э. Рознера 
  и Я. Скоморовского, знакомый со Строком с 1940 г. (тогда он был специальным 
  корреспондентом в Риге), способстовал общению композитора с Утесовым. Новая 
  аранжировка окончательно превращает слоу-фокс в танго, хотя оно и интерпретируется 
  утесовцами несколько маршеобразно. Название «Лунная рапсодия» перекликается 
  со многими другими произведениями — от бетховенской «Лунной сонаты» и «Лунного 
  вальса» Дунаевского до «Серенады лунного света» Гленна Миллера, получившей после 
  1945 г. огромную популярность в СССР. Таким образом в истории этого танго неожиданно 
  обнаруживаются джазовые контексты&quot;. <br>
