<h1>ТВОИ ПИСЬМА</h1>

Обработка Симона Кагана<br>
Слова Бориса Тимофеева<br>
<br>
Их много, конвертов разных<br>
  С знакомым почерком одним.<br>
  Как часто в строках и фразах<br>
  Свою мы нежность радостно храним.<br>
<br>
Широкой лентой связаны,<br>
  Хранились вы года.<br>
  Но приговор мой сказан –<br>
  Прощайте навсегда.<br>
<br>
Камин горит, огнем охваченный,<br>
  В последний раз вспыхнули слова любви.<br>
  В тяжелый час, здесь мной назначенный,<br>
  Своей рукой письма я сожгла твои.<br>
<br>
Нежность, любовь, ласка,<br>
Мечты обо мне.<br>
Писем твоих связка<br>
Уже вся в огне.<br>
В тяжелый час, здесь мной назначенный,<br>
  Своей рукой письма я сожгла твои.<br>
<br>
Мы часто умом зависим<br>
  От слов, что сердце сладко лжет.<br>
  Довольно неверных писем.<br>
  Пускай их пламя жадное сожжет.<br>
<br>
Широкой лентой 
  связаны…<br>
