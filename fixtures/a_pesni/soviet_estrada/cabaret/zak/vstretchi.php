 <br>
  <br>
  Песня написана конце 1930-х годов молодым композитором Ильей Жаком для Клавдии 
  Шульженко. Оба они тогда работали в Ленинграде в джазе Скоморовского: он - пианистом 
  и концертмейстером, она - певицей. Там же работал муж Шульженко куплетист Владимир 
  Коралли. Песня сразу же вышла на пластинке в Ленинграде (ленинградские пластинки 
  имели небольшие тиражи и обращались, в основном, в пределах Ленинграда).<br>
  <br>
  Осенью 1939 года Шульженко едет в Москву на Первый всесоюзный конкурс артистов эстрады, 
  где становится лауреатом (Жак ей аккомпанировал, хотя, по требованию мужа, Шульженко 
  не пела на конкурсе песен Жака). После этого певицу приглашают в Дом звукозаписи 
  на Малой Никитской, где 29 и 31 января 1940 года она записывает с джазом Скоморовского 
  пять своих лучших песен, включая <a href="../../drugije/andrucha.htm">&quot;Андрюшу&quot;</a> 
  и &quot;Встречи&quot;. С этих пластинок, выпущенных Апрелевским и Ногинским 
  заводами, начинается всеобщая известность Шульженко в СССР.<br>
  <br>
  Песня входила также в репертуар Изабеллы Юрьевой.<br>
