	<h1>ЧЕТЫРНАДЦАТЬ 
МИНУТ ДО СТАРТА</h1>

Слова Владимира Войновича<br>
Музыка Оскара Фельцмана<br>
<br>
Заправлены в планшеты<br>
  Космические карты,<br>
  И штурман уточняет<br>
  В последний раз маршрут.<br>
  Давайте-ка, ребята,<br>
  Закурим перед стартом,<br>
  У нас еще в запасе<br>
  Четырнадцать минут.<br>
  <br>
<em>Припев:</em><br>
<br>
  Я верю, друзья,<br>
  Караваны ракет<br>
  Помчат нас вперед<br>
  От звезды до звезды.<br>
  На пыльных тропинках<br>
  Далеких планет<br>
  Останутся наши следы.<br>
<br>
Когда-нибудь с годами<br>
  Припомним мы с друзьями,<br>
  Как по дорогам звездным<br>
  Вели мы первый путь,<br>
  Как первыми сумели<br>
  Достичь заветной цели<br>
  И на родную Землю<br>
  Со стороны взглянуть.<br>
<br>
Давно нас ожидают<br>
  Далекие планеты,<br>
  Холодные планеты,<br>
  Безмолвные поля.<br>
  Но ни одна планета<br>
  Не ждет нас так, как эта,<br>
  Планета дорогая<br>
  По имени Земля. <br>
  <br>
  1960<br>
<br>
<span class='i'>Неизвестный источник</span><br>
<br>
<br>
Песня создана по государственному заказу осенью 1960 г. в ожидании полета первого советского космонавта, который произошел 12 апреля 1961 г. Владимир Войнович, только что ставший сотрудником Всесоюзного радио, вызвался сочинить текст добровольцем, так как больше никто не взялся, - это его первая официальная песня. Музыку предложили написать Оскару Фельцману. Для трансляций по радио песню исполнил Владимир Трошин. В августе 1962 г. песню спели в космосе космонавты Николаев и Попович, а при их торжественной встрече лидер СССР Никита Хрущев процитировал с Мавзолея строки &quot;На пыльных тропинках далеких планет /Останутся наши следы&quot;. После этого песню дважды напечатали в &quot;Правде&quot;. Есть пародия - <a href="../dvor/projdetmnogo.php">&quot;Пройдет много лет&quot;</a>.<br>

