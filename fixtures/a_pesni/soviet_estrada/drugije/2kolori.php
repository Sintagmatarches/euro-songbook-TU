К этой песне было неоднозначное отношение у властей советской Украины, так как черный и красный - цвета  флага Украинской повстанческой армии, в которой воевал автор слов поэт Дмитрий Павлычко, в будущем один из лидеров Перестройки на Украине.<br>
<br>
Песня создана в 1964 г., в исполнении Анатолия Мокренко была выслушана худсоветом Украинского радио и не рекомендована к исполнению из-за последнего куплета, который сочли пессимистическим. По воспоминаниям Павлычко, сами авторы хотели, чтобы песню исполнил знаменитый украинский  певец Дмитрий Гнатюк, которому песню отправили сразу после создания, но тот не ответил. Через полгода, когда Гнатюк готовил концерт к годовщине октябрьской революции (7 ноября), он позвонил Павлычко и попросил, ссылаясь на анонимного &quot;важного человека&quot;, переделать последний куплет. Павлычко отказался. Решение пришлось принимать первому секретарю ЦК Компартии Украины Петру Шелесту, который разрешил исполнение. Гнатюк включил &quot;Два кольори&quot; в репертуар, и она стала самой знаменитой его песней (см. <a href="https://kp.ua/culture/537823-pesnui-dva-kolory-dmytryi-hnatuik-spel-naperekor-khudsovetu-kompartyy" target="_blank">"Комсомольскую правду в Украине", 29 апр. 2016</a>).<br>

