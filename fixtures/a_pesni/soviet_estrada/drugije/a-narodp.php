  На лирику Шаферана писали песни именитые композиторы — Френкель, Мовсесян, Минков 
  (тесно он сотрудничал с Юрием Саульским и Евгением Птичкиным), пели их самые 
  популярные певцы.<br>
  <br>
  Стихи Игорь Шаферан начал писать еще в школе (между прочим, учился в одном классе 
  с М. Жванецким). Сразу после школы поехал в Москву поступать в Литературный 
  институт, но с первого раза не поступил. Год отучился в Тимирязевской академии. 
  А потом Шаферана взял на свой курс знаменитый поэт Михаил Светлов. «По его совету 
  папа и начал писать тексты для песен», — рассказывает Анна Шаферан.<br>
  <br>
  Первой стала написанная еще в студенческие годы вместе с композитором Э. Колмановским 
  песня «Береговые огоньки». А через несколько лет И. Кобзон в Колонном зале Дома 
  Союзов в Москве спел «Мальчишки, мальчишки» А. Островского и И. Шаферана. Тогда 
  к молодому поэту пришла настоящая слава...<br>
  <br>
  Семейная жизнь Шаферана была счастливой, женился он по большой любви всего через 
  неделю после знакомства с дочерью известного архитектора Бориса Виленского. 
  Правда, прожил поэт совсем недолго — всего 62 года.<br>
  <br>
  По мнению дочери, его «убил капитализм». Новые реалии оказались для него страшным 
  психологическим ударом. Лауреат Всесоюзных и Всероссийских конкурсов песни, 
  «народный певец» не отличался деловой хваткой. Ничем другим, кроме поэзии, он 
  не занимался. От ощущения своей ненужности Шаферан заболел, долго лежал в онкологическом 
  центре, но врачи не смогли ничего сделать и, поняв это, отправили его умирать 
  домой. После его смерти многие стихи остались невостребованными.<br>
