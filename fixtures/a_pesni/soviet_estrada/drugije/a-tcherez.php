  <strong>КУЗНЕЦОВ Юрий Анатольевич</strong><br>
  Род. В 1953 в Одессе.<br>
  <br>
  Пианист. Окончил школу им. П. Столярского и Одесскую консерваторию. Одно время 
  преподавал в ней. Лауреат восьми Всесоюзных конкурсов и джазовых фестивалей. 
  После выступления на Всемирном фестивале молодёжи и студентов в Москве, в 1985 
  году, музыкальные критики страны назвали Кузнецова &quot;Открытием года&quot;. 
  Играл с крупнейшими мастерами современного джаза: В. Чекасиным, Т. Найссо, П. 
  Хором, Б. Верде и другими. Считается одним из серьезных джазовых музыкантов.<br>
  <br>
  <strong>ТЕРЕНТЬЕВ С. В.</strong> – одессит, пианист. Выдающийся джазовый музыкант. 
  Один из немногих, кто строит свой репертуар от классической музыки к джазовой. 
  Великолепный импровизатор. Работал в основном, в европейских странах. Редкие 
  встречи с ним – всегда событие для одесситов, ценителей истинного искусства!<br>
  <br>
  - А теперь, Моня, позволь сделать небольшое отступление по данной теме: Сергей 
  Терентьев уже несколько лет, как вернулся в Одессу из Испании, где работал пианистом 
  в Испанском Королевском оркестре. Что-то не сложилось у него в Испании, и он 
  работает аккомпаниатором в одном из ночных клубов Одессы.<br>
  <br>
  Пару лет назад на сайте Всемирного клуба одесситов промелькнуло сообщение о 
  том, что в адрес начальника Облуправления культуры Одессы, каковым на то время 
  был... КУЗНЕЦОВ Юрий Анатольевич, поступило открытое письмо ректора Одесского 
  университета, в котором говорилось, что Сергей Терентьев – национальное достояние 
  одесситов. Поэтому есть просьба: хотя бы раз в месяц выделять Терентьеву зал 
  Филармонии для сольного концерта! Было ли это воплощено в жизнь, или только 
  осталось гласом вопиющего в пустыне - мне неизвестно...<br>
  <br>
  А теперь несколько слов о человеке, знакомство с которым оставило неизгладимый 
  след в моей жизни. Говорить о нём сухим языком короткой справки я не смогу...<br>
  <br>
  <strong>РАДИОЛЛО М. В.</strong><br>
  <br>
  Род. 1940 г. Одессит. Музыковед и исследователь классической 6-тиструнной гитары. 
