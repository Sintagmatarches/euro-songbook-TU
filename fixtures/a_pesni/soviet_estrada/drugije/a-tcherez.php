  <h1>ЧЕРЕЗ ГОДЫ, ЧЕРЕЗ РАССТОЯНЬЯ...</h1>
 
  <span class='i'><a href="http://www.shanson.org/articles/cherez_gody" target="_blank" >Шансон-портал</a>, 2008<br></span>
  <br>
  <br>
  <span class='i'>Уважаемый читатель! Статья «Через годы, через расстоянья...» написана как 
  приглашение одесских музыкантов к разговору о развитии музыкальной жизни в Одессе. 
  В понятие «Музыкальная жизнь» органично входит и ресторанная музыка, и проведение 
  свадеб, юбилеев и других мероприятий. И все такие праздники сопровождались песней. 
  Весёлой или грустной, лирической или трагической, шуточной или блатной. В развитии 
  песенного творчества были спады и подъёмы, но оно никогда не стояло на месте.<br>
  <br>
  Примером тому может считаться грандиозный концерт русской песни, состоявшийся 
  в Париже 2 декабря 2007 года. Вёл концерт Ален Делон. На этом концерте впервые 
  (для Парижа и для Одессы!) были исполнены 2 песни на музыку Константина Швуима. 
  Стихи Ильи Резника. Желающим поближе познакомиться с этой темой можно <a href="http://www.maxime-and-co.com/Radio-Chanson-Djanni-021207.htm" target="_blank">здесь</a>.<br>
  <br>
  Множество одесских музыкантов и певцов волей судьбы рассеяны по всему миру, 
  но от этого они не перестали быть одесситами! И любой из них может сегодня сказать:<br>
  <br>
  - Ви хочете песен? - ИХ есть у мене! <br>
  <br>
  <img src="a-tcherez1.jpg" alt="Всеволод Верник"><br>
  Всеволод Верник</span><br>
  <br>
  <br>
  Лёгкий весенний дождик нисколько не мешал нам с Моней предаваться воспоминаниям, 
  уютно устроившись на диване в квартирке, которую Моня называл «полбедрумной». 
  Это была крошечная студия, перегороженная на две части. Одна из них была гостиной 
  другая – спальней.<br>
  <br>
  - Моня, кто такие клезмеры? - спросил я.<br>
  <br>
  - А ты кого имеешь в виду: Гарика, Петю или Сеню? – сказал Моня. <br>
  <br>
  - Я таки-да знал всех троих, но они уже не были клезмерами, хотя и носили Такую 
  фамилию... Все трое были очень неплохими музыкантами, но – клезмерами – нет!<br>
  <br>
  Моня поднялся с дивана и подошёл к книжному шкафу. Не глядя вынул книгу в темно-вишнёвом 
  переплёте, где золотом было напечатано три слова: «КТО ЕСТЬ КТО», - а на самом 
  верху – «ОДЕССА». Не раскрывая книгу, Моня сказал:<br>
  <br>
  - Эту книгу написал Александр Каменный (он же Штейман Александр Эуардович). 
  Тут собраны имена самых знаменитых и выдающихся деятелей Одессы, не только музыкантов! 
  Но даже если мы уделим каждому музыканту по 3 строчки, на это уйдет не меньше 
  суток! Такой подход может кого-то обидеть, но я, как Козьма Прутков, «не могу 
  объять необъятного»! Итак – о клезмерах!<br>
  <br>
  Так вот - испокон веку в общинах Германии и соседних с ней стран клезмерами 
  называли еврейских народных музыкантов, которые играли на свадьбах, бар-мицвах, 
  праздничных гуляниях, балах, ярмарках, и каждому такому событию соответствовал 
  особый, отточенный годами и поколениями репертуар. Но во всем своем блеске искрометное 
  искусство клезмеров проявилось в городах и местечках Польши, Бессарабии, Галиции, 
  Украины, то есть именно там, откуда тысячи евреев переселились когда-то в Одессу 
  и принесли с собой обычаи, нравы, быт, говор, одежду и музыку. И хоть большинство 
  клезмеров не знали нотной грамоты и были, как говорят музыканты, добротными 
  «слухачами», они передавали потомкам не только веселое свое занятие, но мелодии, 
  а некоторые их них, случалось, становились профессиональными музыкантами.<br>
  <br>
  Легендарным клезмером, имя которого сохранила для нас история, был Йосл Друкер, 
  скрипач. О его игре ходили легенды. За полтысячи вёрст послушать Друкера, по 
  прозвищу Стемпеню, приезжали не только простые евреи, но и молодые раввины. 
  Затаив дыхание, они старались запомнить мелодии Стемпеню и частенько потом вставляли 
  их в свои молитвы. Матери, у которых были дочери на выданьи, молили Всевышнего, 
  чтобы иметь возможность пригласить на свадьбу Стемпеню. Друкер был клезмером 
  чуть ли не в восьмом поколении. Его имя сохранилось до наших дней потому, что 
  Стемпеню стал героем первого романа Шолом Алейхема. Роман так и назывался - 
  «Стемпеню».<br>
  <br>
  Сыном местечкового клезмера и сам поначалу клезмером был легендарный Пейсах 
  Столярский, который, окончив в Одессе музыкальное училище, открыл детскую музыкальную 
  школу, создал уникальную, не утратившую ценность и сегодня, методику развития 
  таланта у одаренных детей и стал, в частности, первым учителем гениального скрипача 
  Давида Ойстраха.<br>
  <br>
  У местечковых клезмеров Подольской губернии учился игре на скрипке Сендер Певзнер, 
  который обосновался потом в Одессе и играл здесь в пивном подвальчике «Гамбринус» 
  на углу Преображенской и Дерибасовской улиц. Обладая несомненным, я бы сказал, 
  специфичным музыкальным дарованием, он, что называется, на лету схватывал мелодию 
  и в усладу публики играл английские, грузинские, еврейские, русские, украинские 
  песни да танцы. И послушать его скрипку специально приходили биндюжники Молдаванки, 
  рабочие Пересыпи, портовые грузчики и иностранные матросы.<br>
  <br>
  Довольно скоро известность Сашки-скрипача, как называли его постоянные посетители 
  «Гамбринуса», переросла в популярность, о чем, кстати, первым написал знаток 
  одесского «дна», литератор, журналист «Одесских новостей» и приятель Владимира 
  Жаботинского Лазарь Осипович Кармен. Александр Иванович Куприн, охочий до пива 
  и колоритных личностей, не просто познакомился с Сашкой, но подружился с ним, 
  полюбил и описал в прекрасном, экранизированном, инсценированном, на многие 
  языки переведенном рассказе «Гамбринус». И вся эта, что устная, что письменная 
  слава ни на копейку не была преувеличена, но Сашка относился к ней если уж не 
  совсем равнодушно, то, во всяком случае, спокойно.<br>
  <br>
  - А знаешь, Моня, я теперь вспоминаю, что однажды, во время обеденного перерыва 
  в музыкальной мастерской, где я учился настройке пианино, мой учитель, Константин 
  Игнатьевич Вересковский (последний по времени аккомпаниатор Певзнера) рассказал, 
  что в конце дня, когда «Гамбринус» был закрыт, Сашка выпивал последний бокал 
  пива и задумчиво говорил: «А между нашими клезмерами с Кричева, так я едва был 
  бы... пиль!»<br>
  <br>
  И это совсем не было рисовкой, или кокетством. Это была трезвая, профессиональная 
  оценка высочайшего мастерства клезмерских «Капелл» и каждого клезмера в отдельности. 
  Специфика клезмерской капеллы была такой, что в ней не было двух одинаковых 
  инструментов, что давало не только свободу в аккомпанементе, но и в импровизации. 
  Видимо, импровизационный стиль клезмерской игры и понравился жителям Нового 
  Орлеана, когда волны погромов, войн и эмиграции в странах Европы «посодействовали» 
  знакомству негритянских ансамблей с клезмерами!<br>
  <br>
  - Правильно, - сказал Моня, теперь понятно, почему Утёсов говорил, что джаз 
  зародился в Одессе! Ошибка кроется в том, что Утёсов слышал только музыкантов 
  Одессы!.. В начале 20-го века клезмерство получило передышку. После того, как 
  в 1904 году предприниматели Готлиб и Розенблит открыли в Одессе первые стационарные, 
  в отличие от прежних передвижных, иллюзионы, сиречь кинотеатры, они начали «завоевывать» 
  город и особенно быстро - Молдаванку. А в каждом таком иллюзионе тогда обязательно 
  был тапёр. Некоторым читателям, которые помоложе, наверное, нужно уже пояснить 
  смысл этой, сегодня совершенно архаичной, профессии. В эпоху же немого кинематографа 
  во время сеанса тапёр, расположившись в зале и поглядывая на экран, играл, чаще 
  всего на фортепиано, фрагменты различных музыкальных произведений, стараясь, 
  чтобы они более или менее соответствовали каждому эпизоду фильма: веселому, 
  грустному, трагичному, драматичному, торжественному... Многие понаторевшие в 
  своем деле тапёры так искусно соединяли эти фрагменты, что получалась живая 
  фонограмма! А мелодии были клезмерскими! Тапёрами были очень многие одесские 
  музыканты, которыми сегодня гордится Одесса. А теперь чуть конкретнее.<br>
