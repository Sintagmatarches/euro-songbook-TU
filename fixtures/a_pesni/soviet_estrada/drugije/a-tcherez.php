  <strong>ГИЛЕЛЬС Эмиль Григорьевич</strong><br>
  Род. 1916 в Одессе.<br>
  <br>
  Пианист. Когда 16-летний Гилельс, которого в Одессе дразнили &quot;Милька Рыжий&quot;, 
  выступил на I Всесоюзном конкурсе исполнителей, зал был ошеломлен. Все поняли, 
  что восходит музыкальная звезда первой величины. В юности любил поиграть в карты 
  в компании творческих работников и очень часто проигрывался. Однажды спросил 
  у своего друга Гриши Колтунова (в будущем - известного кинодраматурга): «Гриша, 
  что делать, чтобы не проигрывать?». «Миля, играй не в карты, а на скрипке», 
  - посоветовал Колтунов. Гилельс окончил Одесскую консерваторию. Обладатель I 
  премии на конкурсе пианистов им. Э. Исае (Брюссель). Лауреат Сталинской и Ленинской 
  премий, Герой Социалистического Труда. Награжден пятью орденами. Однажды после 
  концерта в Кремле к Гилельсу подошел Сталин и сказал: «Ты - мое рыжее золото». 
  Народный артист СССР.<br>
  <br>
  12 сентября 1985 года Гилельс дал в Хельсинки концерт, ставший последним в его 
  жизни. Спустя месяц, 14 октября, он скоропостижно скончался в Москве, не дожив 
  пяти дней до своего 69-летия.<br>
  <br>
  …Он родился и ушел в вечность в октябре — месяце, когда «пышное природы увяданье» 
  покоряет своей нерукотворной красотой. Создаваемая им красота была рукотворна, 
  правда, творить ее мог лишь истинный гений. Таковым и был Эмиль Гилельс — один 
  из виднейших пианистов ХХ века. Один из достойных учеников Берты Михайловны 
  Рейнгбальд – Гениального педагога и человека. Внучки легендарной Любки Казак...<br>
  <br>
