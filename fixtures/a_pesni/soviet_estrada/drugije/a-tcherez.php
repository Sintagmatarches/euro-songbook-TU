  «спасли талант Эмиля». А после того, как в 1933 году, будучи студентом всего 
  лишь 2 курса консерватории, Гилельс одержал блистательную победу на первом Всесоюзном 
  конкурсе музыкантов-исполнителей в Москве, и посыпались любезные приглашения 
  и соблазнительные договоры, Берта Михайловна, бросив всех и всё, ездила вместе 
  с ним исключительно для того, чтобы не прерывать занятия, по возможности сократить 
  количество выступлений и поскорее увезти его обратно в Одессу, к систематической 
  работе.<br>
  <br>
  …Известность окружала Берту Михайловну, слава создателя одесской фортепианной 
  школы всегда была рядом, и сверху, вернее, «с верхов», то и дело сыпались знаки 
  внимания, уважения и признания заслуг. Звание профессора одесской государственной 
  консерватории. Врученный ей в самом Кремле и самим «всесоюзным старостой» Калининым 
  орден Трудового Красного Знамени, а в то время статус орденоносца был еще редок, 
  почетен и полезен. Мандат депутата одесского областного Совета депутатов трудящихся. 
  Подаренный ни больше, ни меньше, как правительством великолепный рояль знаменитой 
  берлинской фирмы «К. Бехштейн», какой и в старое время за бешеные деньги продавался 
  в Одессе исключительно в Торговом доме Рауша на Полицейской улице. Всё это, 
  безо всякого сомнения, было более чем заслужено и, чего уж греха таить, приятно, 
  как приятно вовремя сказанное доброе слово в награду за то, на что жизнь без 
  расчета и раздумий кладется. Но, да простится мне сие, если кому-то покажется 
  кощунственным утверждение о том, что лучше бы не было всех этих публично и громогласно 
  декларированных почестей, которые потом только заострили обиду, нанесенную ей 
  не где-нибудь, а в родном городе, и не когда-нибудь, а в годы войны, когда вселенского 
  горя и без того хватало.<br>
  <br>
  Война — это не только страшная беда, но и чаще всего всеобщая неразбериха. И 
  получилось так, что при всей организованности эвакуации, коей от других городов 
  Одесса отличилась с начала войны и до последнего дня её обороны, ни консерваторию, 
  ни музыкальную школу Столярского почему-то не удосужились вывезти. Берте Михайловне, 
  правда, совместно со Столярским, удалось «достучаться» до высоких местных властей, 
  но в ответ услышала она лишь обвинение чуть ли ни в паникерстве, дескать, о 
  какой эвакуации идет речь, если Одессу мы не собираемся сдавать и не сдадим 
  ни в коем разе. И пришлось потом каждому выбираться из Одессы кто куда мог и 
  у кого как получилось. Петр Соломонович Столярский оказался в Свердловске, Анетта 
  Петровна Бычач, ассистент Берты Михайловны, — в Чкалове, бывшем и нынешнем Оренбурге, 
  а сама она — в Ташкенте.<br>
  <br>
  В Ташкент же, после тяжелой контузии прибыл с фронта бывший курсант Томского 
  артиллерийского училища Алик Рубинштейн, сын Берты Михайловны… Один из друзей 
  Рейнгбальд, выдающийся московский педагог - виолончелист профессор С.М. Козолупов, 
  забрал к себе на обучение ее сына и вырастил настоящего виолончелиста, много 
  лет проработавшего в Москве (ныне, увы, покойного). Алик работал в джазе Эдди 
  Рознера, потом эмигрировал в США и тесно сотрудничал с Голливудом.<br>
  <br>
  Но, как по-одесски звучит народная мудрость, таки-да, нет пророка в своем отечестве, 
  ни профессора нет, ни депутата, ни орденоносца, ни корифея-педагога. Дом, где 
  располагалась ее довоенная квартира, теперь занимал «Смерш», как сокращенно 
  именовали «контору» с категоричным и грозным названием «смерть шпионам». И пришлось 
  потому внучке мадам Любки с Молдаванки, имевшей когда-то в «раньшее время» три 
  дома на Мельничной, не считая дома на Балковской, ночевать в милостиво разрешенном 
  ей консерваторском классе. И обивала она пороги различных учреждений и кабинетов 
  начальственных лиц, добралась даже до хорошо знавшего её по счастливым прежним 
  временам председателя горисполкома и поначалу поверила вроде бы восторженному 
  приему: «Ах, Берта Михайловна! Ах, о чем вы говорите! Вне всякой очереди! Незамедлительно! 
  Только… потерпите ещё парочку дней в консерватории, пока вам подберут квартиру». 
  А ей, измученной, после тифа и дальней дороги через всю воюющую страну, хотя 
  бы комнату какую, где можно голову преклонить, рояль поставить и прописку получить, 
  без которой никак нельзя было тогда ни хлебные карточки получить, ни на работу 
  оформиться, что в консерваторию, что в школу покойного Столярского, куда она 
  Москвой назначена была художественным руководителем. Растянулась эта обещанная 
  «парочка дней» в полуторамесячные хождения по мукам, за каковое время былая 
  уверенность растворилась в надежду, надежда сменилась недоумением, недоумение 
  превратилось в обиду, а обида обернулась отчаянием. И в таком, никому не дай 
  Бог желаемом состоянии, она отправилась, было, к знакомым, поднялась на высокий 
  четвертый этаж, но не к ним зашла, а бросилась вниз к концу всех мучений, и 
  нашли её на площадке первого. Это было 19 октября 1944 года, за две недели до 
  её сорок седьмого дня рождения, а ведь могла еще долго жить и красиво работать. 
  Но не дано было, вернее, не дали, убили, как сказал, узнав о трагедии, Дмитрий 
  Шостакович, потому что для этого совсем не обязателен ни нож и ни пуля, достаточно 
  жестоко обидеть, изощренно наплевать в душу и загнать в самый дальний и тесный 
  угол жизни.<br>
  <br>
  - Моня, неужели так могло быть?!! А как же пресса, Гилельс, наконец? Неужели 
  все вокруг молчали? Лицо Мони пошло красными пятнами. Он посмотрел на меня и 
  ответил: <br>
  <br>
  - Ни одно из двух газетных изданий того времени не нашло пяти сантиметров газетной 
  площади для некролога! Более того! В музыкальном энциклопедическом словаре, 
  выпуска 1959 года фамилии Рейнгбальд - нет! А теперь о Гилельсе...<br>
  <br>
  …Ученики Берты Михайловны вознамерились, было, поставить памятник на её могиле, 
  но, как оно часто бывает, чем больше людей собираются что-либо сделать совместно, 
  тем трудней им договориться. Кончилось тем, что приехал Эмиль Гилельс и поставил 
  изящный беломраморный памятник с лаконичной, лишенной сусальных кладбищенских 
  сантиментов, надписью: «Дорогому учителю и другу».<br>
  <br>
  Но всё это было уже много позже. А тогда, в октябре 1944-го, на трагедию в Одессе 
  соболезнующими телеграммами отозвались коллеги, друзья, знакомые — порядочные 
  люди разных профессий и национальностей: актер Михаил Астангов, историк Милица 
  Нечкина, пианисты Генрих Нейгауз и Яков Флиер, композиторы Арам Хачатурян и 
  Дмитрий Шостакович, академик Филатов, профессор Елена Гнесина, которая еще недавно 
  так уговаривала Б.М. Рейнгбальд остаться в Москве преподавать в основанном ею 
  Московском музыкально-педагогическом институте. Но весть о самоубийстве возвратившейся 
  из эвакуации и оказавшейся бездомной профессора консерватории Рейнгбальд мгновенно 
  разлетелась по городу к великому недовольству власть предержащих.<br>
  <br>
  Осенью 1974 года, когда исполнялось тридцать лет со дня трагической гибели Берты 
  Рейнгбальд, в Одессу специально приехал Эмиль Гилельс. Он хотел дать концерт 
  в память своего учителя и вполне справедливо считал, что это непременно должно 
  значиться на афишах. Однако именно это и явилось главным камнем преткновения. 
  Дошло до решительного телефонного разговора с секретарем обкома партии, который 
  в ответ на требование Гилельса, не задумываясь, заявил, что это невозможно. 
  Великий музыкант, еле сдерживая эмоции, спокойно и вместе с тем твердо отрезал: 
  «В таком случае концерта вообще не будет». Мнивший себя всесильным, функционер 
  смекнул, что дело может обернуться громким скандалом и крушением его личной 
  карьеры. Ведь Гилельс давно уже не был тем робким мальчиком с Молдаванки, каким 
  он пришел когда-то к Берте Михайловне, а всемирно известным музыкантом, народным 
  артистом СССР, лауреатом самой престижной по тем временам Ленинской и вдобавок 
  еще Государственной премии, профессором Московской консерватории. Последовали 
  какие-то «согласующие» переговоры с Москвой, после чего, наконец, в афишах было 
  дозволено напечатать, что концерт играется «в память профессора Б. М. Рейнгбальд». 
  3 ноября Гилельс вышел на сцену филармонии с черной траурной повязкой на рукаве 
  и необычайно проникновенно сыграл самые любимые произведения покойной Берты 
  Рейнгбальд. Историографы подчеркивают, что в прессе не появилось ни одной рецензии 
  на тот концерт, но Гилельса это, скорее всего, уже не волновало: несмотря ни 
  на что, он вышел победителем из той, поначалу совершенно тупиковой ситуации, 
  а победителям не нужны ласки побежденных!<br>
  <br>
  Спустя пятилетие, в 1979-м, музыкант отметил сольным концертом в Одесском оперном 
  театре полувековой юбилей своего первого в жизни сольного концерта. Это было 
  последнее выступление маэстро в родном городе...<br>
