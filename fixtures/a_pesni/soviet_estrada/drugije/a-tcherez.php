  <strong>САКСОНСКИЙ Лазарь Яковлевич</strong> <br>
  Род. 1897 в Одессе.<br>
  <br>
  Пианист. Один из первых выпускников Одесской консерватории. С 15 лет — пианист 
  в театрах и кино Одессы. С 1945 - концертмейстер Одесской филармонии и радио. 
  Автор множества музыкальных произведений, в том числе «Рапсодии на украинские 
  темы», «Белорусской» и «Казахской» рапсодий, романсов. Был очень мудрым и уважаемым 
  в Одессе человеком. 70 лет прожил с супругой, которую называл верной и единственной 
  подругой жизни. Однажды диктор Одесского телевидения Нелли Харченко в одном 
  из первых своих выступлений в роли ведущей, торжественно представив певицу, 
  добавила: «У рояля...» и тут же услышала из-за кулис голос Саксонского: «Стул». 
  Харченко мгновенно сориентировалась: «Партию фортепиано исполняет Лазарь Саксонский».<br>
  <br>
  <strong>ЛИСТОВ Константин Яковлевич</strong><br>
  Род. 1900 в Одессе.<br>
  <br>
  Композитор. Автор опер, оперетт, симфонических сочинений и около 800 песен, 
  среди которых - «Тачанка&quot; (сл. Рудермана), «Гренада» (сл. Светлова), «Жди 
  меня» (сл. Симонова), «Севастопольский вальс» и «В землянке» (сл. Суркова). 
  Награжден орденом Красного Знамени и медалями. Народный артист РСФСР.<br>
  <br>
  <strong>ОЙСТРАХ Давид Федорович</strong><br>
  Род. 1908 в Одессе.<br>
  <br>
  Скрипач. С 5 лет обучался у П. Столярского, под руководством которого окончил 
  Одесскую консерваторию. Лауреат международных конкурсов. Ему посвятили свои 
  произведения композиторы Прокофьев, Мясковский, Хачатурян, Шостакович, Кабалевский 
  и другие. С 1958 Ойстрах возглавлял жюри по скрипке на Международном конкурсе 
  им. П. Чайковского. Лауреат Сталинской и Ленинской премий. Награжден двумя орденами 
  Ленина. Народный артист СССР.<br>
  <br>
  <strong>АЛЬТМАН Анна Михайловна</strong><br>
  Род. 1935 в Одессе.<br>
  <br>
  Пианистка. Окончила Одесское музыкальное училище. Одна из ведущих концертмейстеров 
  филармонии, сотрудничала с театром «Комедиум», неизменный участник капустников 
  в одесском Доме Актёров. Живёт и работает в Израиле с 1991 года.<br>
  <br>
  <strong>ГОЛОЩАПОВ Николай Яковлевич</strong><br>
  Род. в 1941.<br>
  <br>
  Джазовый музыкант, дирижер. Окончил Одесскую консерваторию. 25 лет руководил 
  Народным эстрадным оркестром одесских таксистов «Зелёный огонёк». Руководитель 
  эстрадного отдела музучилища (второго в УССР). Руководитель бигбенда одесского 
  училища и консерватории. Лауреат двух республиканских и многих международных 
  джазовых фестивалей (Ленинград, Регенсбург). Художественный руководитель всех 
  одесских джазовых фестивалей.<br>
