	<span class="header"><strong>ПЕСНЯ 
О ВОЛГЕ</strong><br>
	</span>
Из кинофильма «Волга-Волга» (1938)<br>
<br>
Музыка Исаака Дунаевского<br> 
Слова Василия Лебедева-Кумача
<br>
<br>
Много песен про Волгу пропето,<br>
  Но еще не сложили такой,<br>
  Чтобы, солнцем советским согрета,<br>
  Зазвенела над Волгой-рекой.<br>
  Словно тучи, печально и долго<br>
  Над страной проходили века,<br>
  И слезами катилася Волга –<br>
  Необъятная наша река.<br>
<em><br>
Припев: </em><br>
<br>
Красавица народная, <br>
  Как море, полноводная, <br>
  Как Родина, свободная, — <br>
  Широка, глубока, сильна! <br>
  Не сдавалась цепям и обманам <br>
  Голубая дорога страны,— <br>
  Незадаром Степан с Емельяном <br>
  Вниз по Волге водили челны. <br>
  Много песен над Волгой звенело, <br>
  Да напев был у песен не тот, — <br>
  Прежде песни тоска наша пела, <br>
  А теперь наша радость поет! <br>
<br>
Разорвали мы серые тучи, <br>
  Над землею весна расцвела.<br>
  И, как Волга, рекою могучей <br>
  Наша вольная жизнь потекла!<br>
  Мы сдвигаем и горы и реки, <br>
  Время сказок пришло наяву, <br>
  И по Волге, свободной навеки, <br>
  Корабли приплывают в Москву. <br>
<br>
  От Москвы до ворот волгоградских, *<br>
  Как большая живая рука, <br>
  Все народы приветствует братски <br>
  Всенародная Волга-река. <br>
  Грянем песню и звонко и смело; <br>
  Чтобы в ней наша сила жила, <br>
  Чтоб до самого солнца летела, <br>
  Чтоб до самого сердца дошла! <br>
  <br>
  Наше счастье, как май, молодое, <br>
  Нашу силу нельзя сокрушить. <br>
  Под счастливой советской звездою <br>
  Хорошо и работать и жить <br>
  Пусть враги, как голодные волки, <br>
  У границ оставляют следы, — <br>
  Не видать им красавицы Волги <br>
  И не пить им из Волги воды. <br>
<br>
* Очевидно, первоначально пелось <em>&quot;сталинградских&quot;</em>.<br>
