<br> 
Новый, идеологически выдержанный текст к первому советскому фокстроту <a href="dzongrej-b.php">&quot;Джон Грей&quot;</a> Матвея Блантера и Владимира Масса, написанному в 1923 или 1924 году.<br>
	<br>
	В конце 1960-х годов в беседе с журналистом, будущим историком советской эстрады Глебом Скороходовым Блантер сказал, что &quot;Джона Грея&quot; они в Владимиром Массом написали весной 1923 года. <br><br>
	Был ли фокстрот создан в 1923-м, или нет, но в 1924 году Блантер издал его за свой счет в Москве с текстом Владимира Масса, а позже (ориентировочно, в 1925 году) - с текстом Константина Подревского.  На пластинку фокстрот записан в Москве в 1926 году. <br>
	<br>Он послужил основой  дворовой песни <a href="../dvor/dzongrej.php">об испанце Джоне Грее</a> из страны далекой юга. <br>
	<br>&quot;Джона Грея&quot; с текстом Масса исполнял Андрей Миронов - но только первый куплет.<br>
<br>
<br>
В Российской национальной библиотеке (Санкт-Петербург) есть три авторских издания  фокстрота Блантера: <br>
	<br>
	- московское 1924 года с примечанием на титульном листе &quot;Исполняется с успехом Михаилом Гаркави&quot;, с портретом Гаркави, без указания на титульном листе автора текста и вложенным внутрь листом с текстом Масса;<br>
	<br>
	- киевское 1925 года с указанием на титульном листе автора текста К.Н. Подревского, вложенный листок с текстом в экземпляре библиотеки утрачен; <br>
    <br>
    - ленинградское 1927 года, копирующее киевское издание, с сохранившимся вложенным листом. <br> 
	<br>
	Во всех изданиях над нотами стоит посвящение &quot;Леониду Сигизмундовичу Губер&quot;, на титульном листе и листе с текстом имя персонажа написано как &quot;Джон Гр<strong>е</strong>й&quot;, а над нотами - &quot;Джон Гр<strong>э</strong>й&quot;. Нотная запись во всех трех изданиях абсолютно идентична. <br> 	
