	Музыка Матвея Блантера <br>
Слова Константина Подревского<br>
<br>
Джон Грей — рабочий с дока,<br>
Работы в доке много.<br>
Строго глядит он, на бок сдвинув кепи.<br>
В небе столбом густым<br>
Над ним встал от фабрик дым...<br>
Все в док. Зовет гудок.<br>
<br>
Важно от дока к порту<br>
Едут джентльмены, лорды...<br>
- К чорту. - Джон ворчит им вслед, нахмурив лоб,<br>
- Стоп. Мир — только для рабочих,<br>
Нет места в нем для прочих,<br>
Их — вон. - Так думал Джон.<br>
<br>
Но вот идет авто во весь карьер...<br>
Народ снует, под'ехал сам премьер:<br>
Усы, часы, солидный котелок,<br>
И важен, как бульдог, премьер проходит в док...<br>
- Друзья, я — к вам, не слушайтесь молвы,<br>
Друзья, я сам — рабочий, как и вы...<br>
Я — прост, мой пост не должен вас смущать...<br>
Прошу вас, так сказать,<br>
Не надо бастовать...<br>
<br>
Джон Грей глядит сурово:<br>
Это ему не ново...<br>
- Слова! Болтать я тоже мастер складно.<br>
Ладно, уж если так, -<br>
Пустяк — вам-то скинуть фрак,<br>
К станку встать по свистку.<br>
<br>
Сразу премьер стал хмурым:<br>
- Как же быть с маникюром.<br>
Курам, господа, выходи все на смех...<br>
Эх...<br>
К тому же, как-бы это,<br>
Можно измять манжеты...<br>
Скандал... И замолчал...<br>
<br>
Долой. Тотчас в толпе раздался гул...<br>
- Ой, ой... Что б вас... Премьер дрожа шепнул,<br>
- Друзья, ведь я... Но выдержать не мог,<br>
И важен, как бульдог, премьер покинул док.<br>
Вам впрок про док рассказ: как раз в сезон...<br>
Персон всех вон: пусть бросят свой фасон.<br>
Народ возьмет всю власть на свой манер,<br>
Как это, например,<br>
У нас в С.С.С.Р.<br>
