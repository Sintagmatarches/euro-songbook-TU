	<h1>ЕДУТ НОВОСЕЛЫ</h1>

	Музыка Евгения Родыгина<br>
  Слова Нины Солохиной<br>
  <br>
  Родины просторы, горы и долины,<br>
  В серебро одетый, зимний лес грустит.<br>
  Едут новоселы по земле целинной,<br>
  Песня молодая далеко летит.<br>
  <br>
<span class='i'>Припев:</span>	<br>
	<br>
	Ой, ты, зима морозная,<br>
  Ноченька яснозвездная!<br>
  Скоро ли я увижу<br>
  Мою любимую в степном краю?<br>
  Вьется дорога длинная…<br>
  Здравствуй, земля целинная,<br>
  Здравствуй, простор широкий,<br>
  Весну и молодость встречай свою!	<br>
	<br>
	Зашумят метели, затрещат морозы,<br>
  Но друзей целинных не легко сломить.<br>
  На полях бескрайных вырастут совхозы,<br>
  Только без тебя немножко грустно будет жить.	<br>
	<br>
	Ты ко мне приедешь раннею весною<br>
  Молодой хозяйкой прямо в новый дом.<br>
  С голубым рассветом тучной целиною<br>
  Трактора мы вместе рядом поведем.	<br>
  <br>
1955	<br>
	
