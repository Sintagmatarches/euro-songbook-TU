   <br> 
   Музыка Валентина Кручинина<br>
   Слова Ольги Фадеевой<br>
    <br>
    1. В перелеске, за селом, <br>
Есть у нас аэродром. <br>
Мы с подружками частенько <br>
В гости лётчиков зовем. <br>
А сперва они гордились, <br>
С нами вовсе не водились, <br>
Чо же делать, как нам быть, - <br>
Стали мы им говорить: <br>
<div style="margin-left:32px">«Не высоко ль залетели? <br>
Зря обходите вы нас! <br>
Неужели в самом деле <br>
Все девчата хуже вас? <br>
Неужели в самом деле, <br>
Неужели в самом деле <br>
Все девчата хуже вас?» </div>
 <br>
2. Те, конечно, как могли, <br>
Оправдание нашли: <br>
«Мы-де по небу летали, <br>
Поотвыкли от земли! <br>
Разговор, как начинаем, <br>
Про полёты вспоминаем, <br>
Интересного там нет…» <br>
Но сказали мы в ответ: <br>
<div style="margin-left:32px">«Не высоко ль залетели? <br>
Зря конфузите вы нас! <br>
Неужели в самом деле <br>
Понимаем хуже вас? <br>
Неужели в самом деле, <br>
Неужели в самом деле <br>
Понимаем хуже вас?» </div>
 <br>
3. Стали парни говорить: <br>
«Видно, надо уступить! <br>
Это нам не мессершмитты, <br>
Тут атаки не отбить! <br>
Но вперёд предупреждаем: <br>
Мы частенько улетаем, <br>
Помнить будете иль нет?» <br>
И сказали мы в ответ: <br>
<div style="margin-left:32px">«Не высоко ль залетели? <br>
Обижаете вы нас! <br>
Неужели в самом деле <br>
Память хуже, чем у вас? <br>
Неужели в самом деле, <br>
Неужели в самом деле <br>
Память хуже, чем у вас? <br>
Не высоко ль залетели?» </div>
