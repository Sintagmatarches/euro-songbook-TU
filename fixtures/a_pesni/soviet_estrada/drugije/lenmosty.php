<h1>ЛЕНИНГРАДСКИЕ МОСТЫ</h1>

Музыка Модеста Табачникова<br>
Слова Виктора Драгунского и Людмилы Давидович<br>
<br>
Когда мы были молоды,<br>
Бродили мы по городу, <br>
Встречали мы с подружками рассвет.<br>
Свиданья назначали мы, <br>
И все тогда считали мы, <br>
Что лучше моста места встречи нет!<br>
<br>
В те дни неповторимые<br>
Один встречал любимую <br>
Почти всегда на Каменном мосту. <br>
Другой, придя заранее<br>
На первое свидание, <br>
На Троицком стоял как на посту. <br>
<br>
У фонарей Дворцового<br>
Встречались Вера с Вовою, <br>
А Коля, тот бежал на Биржевой -<br>
Летел для встречи с Катею,<br>
У всех была симпатия, <br>
У каждого был мост любимый свой. <br>
<br>
Весною незабвенною <br>
И я встречался с Леною, <br>
И наш маршрут был трогательно прост: <br>
Купив букет подснежников,<br>
влюбленные и нежные, <br>
Мы шли всегда на Поцелуев мост!<br>
<br>
Стареем неизбежно мы, <br>
но с Леною по-прежнему <br>
Друг в друга влюблены. А в чем секрет? <br>
А в том, признать приходится, <br>
Что все мосты разводятся, <br>
А Поцелуев, извините, нет!<br>
<br>
