<h1>НЕ ЗАБУДЬ</h1>

Музыка Владимира Сидорова<br>
Слова Александра Уманского<br>
<br>
Когда пришла пора разлуки<br>
И грусть невольно сжала грудь,<br>
Ты молвил только: «До свиданья»,<br>
А я сказала: «Не забудь».<br>
<br>
<span class='i'>Припев:<br></span>
<br>
Случайно встретясь с тобой на матче,<br>
Мы разошлись, быть может, навсегда,<br>
Но это ровно ничего не значит:<br>
У всех свой путь, мой друг,<br>
У всех своя мечта.<br>
<br>
И часто, часто, вспоминая<br>
Все то, что больше не вернуть,<br>
Я снова слышу: «До свиданья».<br>
И повторяю: «Не забудь».<br>
<br>
А ты, мой друг, припоминаешь,<br>
Как, собираясь в дальний путь,<br>
Ты говорил мне: «До свиданья»,<br>
А я шепнула: «Не забудь».<br>
<br>
<span class='i'>Припев.<br>
<br>
Я люблю тебя, жизнь: Песни на все времена / Сост. Л. Сафошкина, 
В. Сафошкин. М.: Эксмо, 2004.</span><br>
<br>
<br>
Фокстрот 1940 года из репертуара Клавдии Шульженко.<br>
