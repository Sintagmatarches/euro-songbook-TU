<span class='bold'>Василий Иванович Агапкин</span> (1884-1964) - военный дирижер, композитор. 
  В 1894 г. зачислен музыкантским воспитанником в духовой оркестр 308-го Царевского резервного 
  батальона Астраханского пехотного полка, в той же должности в Аварском резервном пехотном батальоне (1899) и 82-м пехотном Дагестанском полку (1900), Драгунском Северском полку, 43-м и 16-м полках (1901-1906). 1906-1909 - срочная служба в Тверском драгунском полку (Тифлис). 1910-1917 - сверхсрочная служба штаб-трубачом в 7-й запасном кавалерийском 
  полку (Тамбов). Осенью 1911 г. принят в класс медных духовых инструментов Тамбовского 
  музыкального училища, которое окончил в 1915. 1918-1920 гг. - добровольно в Красной Армии, капельмейстер 1-го Красного гусарского Варшавского полка. С конца 1920 г. руководил музыкальной 
  студией и оркестром войск ГПУ в Тамбове. В августе 1922 г. переехал в Москву. 
  Его оркестр участвовал в похоронах Ленина. С началом Великой Отечественной войны 
  Агапкину было присвоено звание военного интенданта 1-го ранга, и он был назначен 
  начальником духовых оркестров отдельной мотострелковой дивизии имени Дзержинского. 
  7 ноября 1941 г. на параде войск, оборонявших Москву, Агапкин дирижировал 
  сводным оркестром. 24 июня 1945 г. на Параде Победы его оркестр входил в состав 
  сводного оркестра. 1947-1955 гг. - начальник образцового оркестра МГБ.<br>
