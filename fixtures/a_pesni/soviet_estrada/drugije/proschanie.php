  КАК &quot;ПРОЩАНИЕ СЛАВЯНКИ” СОЕДИНИЛО ДВЕ ЮБИЛЕЙНЫЕ ДАТЫ<br></span>
<br>
  <span class="i">Газета <a href="http://www.vmdaily.ru/old/23823/23823birukov1.htm" target="_blank">&quot;Вечерняя 
  Москва&quot;</a>, 10 февраля 2004, №25 (вместе с текстом &quot;Прощания славянки&quot; 
