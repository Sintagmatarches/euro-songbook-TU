	<h1>ПРОЩАНИЕ 
  СЛАВЯНКИ</h1> <br>
  Марш написан в Тамбове штаб-трубачом 7-го запасного кавалерийского полка
  Василием Ивановичем Агапкиным (1884-1964), вероятно, в конце 1912 г. как отклик на Первую Балканскую войну (9 октября 1912 - 30 мая 1913). В ее ходе 
  страны Балканского союза (Болгария, Сербия, Греция и Черногория) отвоевали у 
  Турции оккупированные ей несколькими веками ранее балканские территории. Однако 
  мир не воцарился, и уже через месяц между Болгарией и бывшими союзниками вспыхнула 
  Вторая Балканская война, а через год началась Первая мировая, в ходе которой марш и получил известность.<br>
  <br>
  Марш Агапкина был создан для военного духового оркестра, без слов, ноты напечатаны в 1913 г. в Москве в типографии В. Гроссе, вероятно, за счет самого автора. Все тексты появились 
  позднее. На пластинке выпущен фирмой &quot;Экстрафон&quot; летом 1915 г. в 
  Киеве. Самое известное издание марша - с гусаром и женщиной на обложке - вышло в издательстве Циммермана, вероятно, в 1915 г. Эта иллюстрация повторяет семейный фотопортрет Агапкина 1915 года.<br>
  <br>
  В начале Первой мировой войны (скорее всего, в 1915) на мелодию марша была создана 
  песня студентов-добровольцев &quot;Вспоили вы нас и вскормили&quot;. В годы 
  Гражданской она перешла в белые армии - см. добровольческую <a href="../grvojna/bel/vspoilivynas.php">&quot;Вспоили 
  вы нас и вскормили&quot;</a> и песню Сибирской Народной армии <a href="../grvojna/bel/sibirmarch.php">&quot;Сибирский 
  марш&quot;</a>. Причем сам Агапкин в Гражданскую воевал в рядах красных. Еще 
  одна песня Первой мировой на мотив &quot;Прощания славянки&quot; - <a href="../ww1/ponerovnym.php">&quot;По 
  неровным дорогам Галиции...&quot;</a> <br>
  <br>
  В 1937 г. на эту музыку была написана польская солдатская песенка &quot;Расшумелись 
  плачущие березы&quot; (&quot;Rozszumiały się brzozy placzące&quot;, sł. R. Ślęzak), 
  которая около 1943 г., после изменений в тексте, сделанных неизвестным автором, 
  стала одной из самых популярных песен польского Сопротивления &quot;Расшумелись 
  плакучие ивы&quot; (<a href="../ww2-polsk/wierzby.htm">&quot;Rozszumiały się 
  wierzby placzące&quot;</a>), где девушка прощается с уходящими в лес партизанами:<br>
  <br>
  <em>&quot;...Пусть дорога не кончена наша,<br>
  И не знаем, где странствий конец,<br>
  Мы уверены в этой победе:<br>
  Столько пролито крови и слёз...&quot;</em> <br>
  <br>
  В межвоенную эпоху в стране вышло несколько нотных публикаций марша, 
   он входил в репертура военных оркестров, а в 1944 г. появился на пластинке Апрелевского завода с записью оркестра 
  п/у И. В. Петрова (АП12334/12335).<br>
  <br>
  Достоверно неизвестно, исполнялся ли марш на параде в Москве 7 ноября 1941 г., 
  оркестром которого дирижировал Агапкин, так как на фонограмму парад записан 
  не был, документы со списком исполненных произведений огласке 
  не преданы, если вообще такие документы есть. Версия об исполнении марша на 
  легендарном параде базируется на воспоминаниях очевидцев и современников, среди 
  которых генерал-майор Николай Назаров, в 1958-1976 гг. занимавший пост главного 
  военного дирижера Вооруженных Сил СССР, и дочь Агапкина Аза Свердлова.<br>
  <br>
  В 1957 г. марш был использован в фильме &quot;Летят журавли&quot;, где прозвучал 
  в сцене проводов добровольцев на фронт Второй мировой. К 1950-м гг. относится 
  и новый бум сочинения текстов к маршу.<br>
  <br>
  Тогда появились песенка стиляг <a href="../stil/votkomne.php">&quot;Вот ко мне 
  приближается женщина...&quot;</a> и студенческая песня о военных сборах <a href="../army/otgremeli.php">&quot;Отгремели 
  весенние грозы&quot;</a> (следом <a href="../dvor/otgremeli-tur.php">переделанная 
  туристами</a>). Автор &quot;Отгремели весенние грозы&quot; Анатолий Чеповецкий 
  утверждает, что написал ее летом 1953 г., еще до фильма &quot;Летят журавли&quot;.<br>
  <br>
  На этот же мотив обычно поется диссидентская песенка <a href="../dvor/kommpojmali.php">&quot;Коммунисты 
  поймали мальчишку&quot;</a> на слова Николая Вильямса. Само стихотворение было 
  написано в 1969 г., опубликовано в эмигрантском парижском журнале в начале 
  1980-х гг. и исполнялось на разные мелодии. Песенку 
  Вильямса, в свою очередь, в 1990 г. переделали причерноморские археологи в 
  шуточный <a href="../profes/mirmek.php">&quot;Гимн Мирмекистов&quot;</a>.<br>
  <br>
  Также есть жизнеутверждающий сатирический <a href="../bard/drugije/mitkmarch.htm">&quot;Митьковский 
  марш&quot;</a> Дмитрия Шагина:<br>
  <br>
  <em>&quot;...Вставай, браток, вставай!<br>
  Рваный тельничек свой надевай...&quot;</em><br>
  <br>
  Из советского песенного наследия на мотив &quot;Прощания славянки&quot; можно 
  еще упомянуть песенку про вдову и пионера <a href="../dvor/poprostoram.php">&quot;По 
  просторам столичного парка...&quot;</a>. <br>
  <br>
