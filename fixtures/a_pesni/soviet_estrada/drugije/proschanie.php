		Ниже дана версия Юрия Бирюкова о происхождении марша, которая фактами не подтверждается. Не обнаружено никаких документов или воспоминаний о поездке Агапкина в Симферополь, встрече с Яковом Богорадом, участии последнего в редактировании и об издании марша в Симферополе, хотя Богорад, известный оркестровщик и издатель, вел подробные записи о своих клиентах, и записи эти сохранились. Обложка с гусаром и дамой принадлежит более позднему изданию Юлия Циммермана (опубликована в изд.: Соколов В.В. Прощание славянки. М.: Советский композитор, 1987). В СССР марш не запрещался. Песня <a href="../rus-jap/ahzatchem.php">&quot;Ах, зачем нас забрили в солдаты&quot;</a> публиковалась - по крайней мере, в советское время (см., например: А.В. Гуревич и Л.Е. Элиасов. Старый фольклор Прибайкалья. Том первый. Улан-Удэ, 1939. №176. С. 304; Л.Е. Элиасов. Фольклор Восточной Сибири. Часть III. Локальные песни. Улан-Удэ, 1973. С. 395).<br>
  <br>
