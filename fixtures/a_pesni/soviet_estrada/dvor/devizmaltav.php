	<h1>ДЕВУШКА 
ИЗ МАЛЕНЬКОЙ ТАВЕРНЫ</h1>
<br>
Девушку из маленькой таверны<br>
Полюбил суровый капитан<br>
За глаза пугливой дикой серны,<br>
За улыбку, как морской туман.<br>
Полюбил за пепельные косы,<br>
Алых губ нетронутый коралл,<br>
В честь которых бравые матросы<br>
Осушили не один бокал.<br>
<br>
С берегов, похожих на игрушки,<br>
Где коврами бархат на лугах,<br>
Для нее скупались безделушки,<br>
Ожерелья, кольца, жемчуга.<br>
Каждый год с апрельскими ветрами<br>
Из далеких океанских стран<br>
Белый бриг, наполненный дарами,<br>
Привозил суровый капитан.<br>
<br>
А она с улыбкой величавой<br>
Принимала ласки и привет,<br>
Но однажды гордо и лукаво<br>
Бросила безжалостное «нет»…<br>
Он ушел, суровый и жестокий,<br>
Не сказав ни слова в этот миг,<br>
А наутро в море на востоке<br>
Далеко маячил белый бриг.<br>
<br>
И в тот год с весенними ветрами<br>
Из далеких океанских стран<br>
К берегам приветливым и теплым<br>
Не вернулся больше капитан.<br>
Девушка из маленькой таверны<br>
Целый день сидела у окна,<br>
И глаза пугливой дикой серны<br>
Налились слезами дополна.<br>
<br>
И никто не понимал в июне,<br>
Почему в заката поздний час<br>
Девушка из маленькой таверны<br>
Не сводила с моря грустных глаз.<br>
И никто не понимал в июле,<br>
Даже сам хозяин кабака:<br>
Девушка из маленькой таверны<br>
Бросилася в море с маяка.<br>
<br>
Так погибли пепельные косы,<br>
Алых губ нетронутый коралл,<br>
В честь которых бравые матросы<br>
Осушили не один бокал…<br>
