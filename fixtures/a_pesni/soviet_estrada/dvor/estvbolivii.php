        Первоначальный вариант создан Матвеем Блантером и Н. Никером, вероятно, в 1922 г., исполнялся Риной Зеленой. Авторское заглавие - &quot;Сильнее смерти&quot;. Самые ранние издания, доступные в Российской государственной библиотеке - 1924 г. (авторское издание самого Блантера и издание &quot;Молодого издательства&quot;). Еще один текст сочинил В.Н. Владимиров, он указан на обложке авторского издания 1927 г., сам текст не был впечатан в под нотными строками, помещен на вложенном листке.<br> 
        <br> Батавия - порт на острове Ява, столица Голландской Ост-Индии. В 1942 г. городу 
        возвращено историческое название Джакарта, которое он носил до захвата 
        голландцами в 1619 г. Ныне Джакарта - столица Индонезии.<br>
        <br>
        Есть другие фольклорные географические варианты песни (см. ниже): &quot;Есть в Баварии 
        маленький дом&quot;, &quot;Есть в Боливии маленький дом&quot;, &quot;Есть 
        в Италии маленький дом&quot;, &quot;Есть в Лиссабоне маленький дом&quot; 
        и так далее.
        С точки зрения здравого смысла из этих вариантов больше всего подходит 
        первоначальная Батавия. Поклонникам 
        абсурда лучше петь про Баварию, так как эта область (юг современной Германии, 
        главный город - Мюнхен), выхода к морю никогда не имела, а потому матросов (а в начале XX в. - и чернокожих)
        там встретить трудно. Боливия тоже выхода к морю не 
        имеет.<br>
        <br>
        В Псковской археологической экспедиции Эрмитажа в начае 1960-х гг. бытовал 
