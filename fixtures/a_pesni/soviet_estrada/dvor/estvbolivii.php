        <br>
        Есть в Боливии маленький дом,<br>
        Он стоит на обрыве крутом.<br>
        В этом доме в двенадцать часов<br>
        Старый негр отпирает засов. <br>
        <br>
        И мелькает за тенями тень,<br>
        И скрипит под ногами ступень.<br>
        И редеет задумчивый мрак<br>
        От ночного разбоя и драк. <br>
        <br>
        Из-за пары распущенных кос,<br>
        Что пленяли своей красотой,<br>
        С оборванцем подрался матрос,<br>
        Ободряемый шумной толпой. <br>
        <br>
        И сплелися два тела, дрожа<br>
        Под грохочущий моря прибой,<br>
        И сверкнули два острых ножа,<br>
        И смертельный тут начался бой. <br>
        <br>
        На колено бродяга привстал,<br>
        Чтоб врага хоть в лицо увидать.<br>
        В нем он брата родного узнал,<br>
        Не пришлось ему раньше узнать. <br>
        <br>
        И ревела безумно толпа,<br>
        И рыдал оборванец босой.<br>
        Только тихо стояла она,<br>
        Белокурой играя косой. <br>
        <br>
        И последний раз ножик блеснул<br>
        Под рокочущий моря прибой.<br>
        Тут же рядом навеки уснул<br>
        Брат матроса, бродяга босой. <br>
