        <h1>ЕСТЬ В БАТАВИИ МАЛЕНЬКИЙ 
        ДОМ</h1>
        Есть в Батавии маленький дом,<br>
        Он стоит на утесе крутом.<br>
        В этом доме в двенадцать часов<br>
        Слуга-бой отпирает засов. <br>
        <br>
        И за тенью является тень,<br>
        И скрипит под ногами ступень.<br>
        И за час до скандалов и драк<br>
        Там царит предрассветный мрак. <br>
        <br>
        Из-за пары распущенных кос,<br>
        Что пленили своей красотой,<br>
        С оборванцем подрался матрос,<br>
        Ободряемый шумной толпой. <br>
        <br>
        Оборванец был молод-силен,<br>
        В нем кипела, играла любовь,<br>
        А матрос был чахоткой сломлен,<br>
        С горла хлынула алая кровь. <br>
        <br>
        И сцепились два тела, дрожа,<br>
        И сверкнули два острых ножа,<br>
        И с кровавою пеной у губ<br>
        Повалился на землю труп. <br>
        <br>
        Оборванец над трупом присел.<br>
        Что в лице он врага увидал?<br>
        Он в нем брата родного узнал.<br>
        Не пришлось ему раньше узнать. <br>
        <br>
        Ликовала, гудела толпа,<br>
        И рыдал оборванец босой.<br>
        Ну, а рядом стояла она,<br>
        Шаловливо играя косой. <br>
        <br>
        И тогда оборванец вскочил,<br>
        Нужных слов ей сказать не сумел,<br>
        Острый нож он ей в сердце вонзил.<br>
        Та упала под ноги ему. <br>
        <br>
        Ликовала-гудела толпа,<br>
        И рыдал оборванец босой,<br>
        Рядом тихо лежала она<br>
        С перекинутой набок косой.<br>
        <br>
