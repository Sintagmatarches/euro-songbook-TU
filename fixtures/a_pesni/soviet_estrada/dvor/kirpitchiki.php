       Сохранился любопытный, полагаю, уникальный экземпляр пластинки, 
        вышедшей в 1925 году… Записан на этом «тяни-толкае» народный хор под управлением 
        Федора Алехина. С одной стороны — «Пошла Маша за водицей», с другой — 
        русская песня «Кирпичики». Музпред охотно писал хоры. Алехинский коллектив, 
        судя по дошедшим до нас дискам, исполнял не столько народные песни, сколько 
        современный репертуар: большую хоровую фантазию «Смычка города с деревней», 
        революционные песни (так на этикетках) «Даешь мотор» и «Лишь ты рабочий 
        и крестьянин», комсомольские — «Проводы» и «Дуня». «Кирпичики» в этом 
        длинном ряду — единственное произведение, означенное как «русская песня», 
        то есть народная. Судьба ее весьма примечательна. <br>
        <br>
        Пожалуй, другого такого фаворита у Музпреда не было. Запись «Кирпичиков» 
        появилась на пластинках с той же оперативностью, что и фрагменты из «Баядеры» 
        — на следующий день после премьеры. Но одним исполнителем дело не ограничилось. 
        Через несколько месяцев Музпред повторяет запись — на этот раз «Кирпичики» 
        представляет одна из популярных исполнительниц цыганского жанра Нина Дулькевич.* 
        В каталогах 20-х годов значилось более десятка ее старых и новых матриц. 
        И если в 1922 году начинающий сатирик В. Лебедев (будущий Лебедев-Кумач) 
        напечатал в «Жизни искусства» эпиграмму на Нину Васильевну: <br>
        <br>
        Поет со сцены <br>
        И в граммофоне, <br>
        Без перемены, <br>
        Все в том же тоне! <br>
        <br>
        то теперь «Кирпичики» внесли в репертуар певицы новый тон. <br>
        <br>
        Мало того, Музпред выпускает инструментальный вариант фаворита, записанный 
        известным трио баянистов театра Мейерхольда, со сцены которого «Кирпичики» 
        прозвучали в современном «Лесе» Островского, как своеобразное знамение 
        дня нынешнего. <br>
        <br>
        «Песню о кирпичном заводе» («Кирпичики») написали в 1924 году поэт Павел 
        Герман и композитор Валентин Кручинин (последний использовал мелодию вальса 
        С. Бейдлинзона «Две собачки» из цикла оркестровых пьес «Цирковые впечатления»). 
        Народной она стала благодаря своему бурному, взрывному успеху. Повторилась 
        история со шлягером 1911 года «Маруся отравилась» сочинения Я. Ф. Пригожего. 
        Записанная сразу же несколькими фирмами, «Маруся» уже через год потеряла 
        авторство, и пластиночные этикетки называли ее народной. «Русская народная 
        песня» — такой подзаголовок стоял и на тексте Пригожего, прилагаемом к 
        каждому диску «Сирены-рекорд». В двадцатые годы это «лучшее украшение 
        сезона» продолжало печататься в старой записи С. П. Садовникова. <br>
        <br>
        Можно по-разному относиться к такого рода сочинениям. С. Я. Маршак, поэтический 
        вкус которого вне сомнений, считал, что «Маруся» — «баллада с законченным 
        сюжетом, продукт большой культуры». Члены Ассоциации пролетарских музыкантов 
        объявляли подобные баллады мещанскими и низкопробными и объясняли их успех 
        у слушателей (отрицать его было невозможно) пословицей «На безрыбье и 
        рак рыба». Так, орган АМП журнал «Музыка и Октябрь» в январе 1926 года 
        рассказывал с нескрываемой горестью: «В одну из деревень Курской губернии 
        приехавший из Москвы комсомолец завез модную (к сожалению) песню «Кирпичный 
        завод». Через две недели эту песню пели все окрестные деревни. Этот факт 
        показывает, как велика тяга в деревне к революционной песне и как низко 
        качество песни, которую она получает». Не вступая в споры о качестве «Кирпичиков», 
        отметим два обстоятельства, на наш взгляд, предрешивших ее успех: песня 
        эта точно соответствовала канонам городского романса и баллады — с последовательным, 
        подробным изложением сюжета, с мелодраматическими переплетениями и переживаниями 
        и т.д. В ней — гармония формы и содержания. Это во-первых. И во-вторых, 
        «Кирпичный завод» оказался первой бытовой песней нашего времени, и повествовала 
        она не о предприятии, а о людях с легко узнаваемыми заботами и чувствованиями. 
        От своих прародителей она отличалась одним. И «Маруся», и «Раскинулось 
        море», и другие, подобные им, заканчивались обычно трагически — финал, 
        вызывающий слезы сочувствия и сострадания, стал здесь обязательным. «Кирпичики» 
        имели выход оптимистический — герои обретали счастье и находили свое место 
        в новом обществе. Думается, и это сказалось на популярности баллады. <br>
        <br>
        Как точный признак этой популярности — появление «ответов», «продолжений», 
        пародий. И уж совсем в духе нашей истории — фильм, поставленный «по мотивам» 
        шлягера, прозвучавшего из граммофона. (Вспомним дореволюционные картины 
        «У 4 камина», «Бал господен», «Так безумно, так страстно хотелось ей счастья» 
        и, конечно, «Марусю» которая отравилась.) Лента «Кирпичики» вышла на экраны 
        в декабре 1925 года, на пике успеха музпредовских дисков. В главных ролях 
        — звезда «великого немого» Петр Бакшеев и молодая актриса вахтанговской 
        студии Варвара Попова (в фильме ее героиню назвали Марусею — дань традиции?). 
        В качестве рекламы с эстрады, по радио, перед входом в кинотеатры распевались 
        куплеты на мотив той же баллады: <br>
