Руки вымою и бегу к нему<br>
В мастерскую, накинув платок. <br>
<br>
Миловалась с ним, я как водится, <br>
Развеселым он мальчиком был. <br>
И сама того не заметила, <br>
Как он тоже меня полюбил. <br>
<br>

Но была тогда безработица.<br>
По заводу ударила вдруг.<br>
Сенька вылетел, а за ним и я <br>
И еще двести семьдесят душ. <br>
<br>
Тут война пошла буржуазная.<br>
Озверел, обозлился народ. <br>
И по камушкам, по кирпичикам<br>
Растащили кирпичный завод. <br>
<br>
Сенька кровь свою проливал в бою, <br>
За Россию он жизнь отдал<br>
И несчастную всю судьбу мою<br>
Он, как жженый кирпич, поломал...<br>
