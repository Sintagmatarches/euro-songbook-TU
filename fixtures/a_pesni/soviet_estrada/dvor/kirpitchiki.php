        &quot;Кирпичики&quot; открыли жанр песен &quot;нового быта&quot;, 
        который потеснил в середине 1920-х гг. кафешантанную экзотику  
        типа <a href="dzongrej.php">&quot;Джон Грей&quot;</a> или <a href="mitchmandzon.php">&quot;Мичман 
        Джон&quot;</a>. Другие назвагия - &quot;Песня о кирпичном 
        заводе&quot; и &quot;Кирпичный завод&quot;. <br>
        <br>
        В основе мелодии - вальс С. Бейлезона &quot;Две собачки&quot;. Песня появилась 
        в 1923-24 гг., впервые издана в 1924 г. и уже в том же году имела оглушительный 
        успех - это, в общем, всё, что известно достоверно, так как конкретные 
        обстоятельства создания в рзных источниках расходятся.<br>
        <br>
        По наиболее популярной версии, отправной точкой является спектакль Мейерхольда 
        по Островскому &quot;Лес&quot;, где звучал вальс &quot;Две собачки&quot; 
        (премьера - 19 января 1924 г.). Услышав вальс, поэт 
        Павел Герман и композитор Валентин Кручинин якобы решили написать песню, 
        первый написал текст, второй сделал аранжировку. По другой версии, они 
        написали песню еще до премьеры &quot;Леса&quot; в 1923 г. для театра 
        варьете &quot;Павлиний хвост&quot;, где она в том же году и исполнена. <br>
        <br>
        Источником вдохновения 
        для Германа могла быть фабричная песня &quot;Заводы кирпичные&quot; (сложена 2-стопным цезурированным тактовиком, литературной имитацией которого стал &quot;кольцовский&quot; пятисложник, использованный в нечетных строках &quot;Кирпичиков&quot;):<br>
        <br>
        <em>Вы заводы мои,<br>
        Заводы кирпичные,<br>
        Горемычные<br>
        ...<br>
        Ну, а кто же вас разорил?<br>
        Разорила нас, разорила нас<br>
        Красная девица...</em><br>
        <br><br>
        Существовал текст &quot;Кирпичиков&quot; на ненецком языке, сочиненный в 1928 г. Васей Терентьевымм, жителем Ямала, сыном заведующего факторией. Вася был проводником Ямальской экспедиции 1928-29 гг., организованной Комитетом Севера Уральского облисполкома для оценки хозяйственного потенциала Ямала. Текст зачина песни приводится в воспоминаниях начальника экспедиции этнографа В.П. Евладова (Евладов В.П. По тундрам Ямала к Белому острову. Экспедиция на Крайний Север полуострова Ямал в 1928-1929 гг. Тюмень: ИПОС СО РАН, 1992. С. 210.):<br>
        <em><br>
        &quot;Этот талантливый паренек знал, не считая, конечно, русского, три языка - ненецкий, хантыйский и коми-зырянский. Как-то раз он перевел на ненецкий язык известную русскую песню &quot;Катюша&quot; [опечатка, должно быть &quot;Кирпичики&quot; - прим. a-pesni] и часто распевал ее в чумах у костра. Ненцы обычно поют монотоннные песни о том, что было с ними, что видят вокруг, о былой жизни. Слушая Васино пение о &quot;разрушенном заводе&quot; и о восстановлении его &quot;по кирпичику&quot;, тундровые жители принимали его рассказ за правду...<br>
