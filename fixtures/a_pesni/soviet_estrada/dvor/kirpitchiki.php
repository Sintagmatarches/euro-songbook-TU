       Как бы мы сегодня не относились к событиям тех лет, все же должны 
        признать, что так называемая «цыганщина» и экзотические поделки о далеких, 
        знойных странах и бравых «мичманах Джонсах» вошли в противоречие с реально 
        изменившимся бытом и духом новой эпохи. Требовался новый репертуар. Первыми 
        это почувствовали композитор Валентин Кручинин и поэт-песенник Павел Герман 
        — авторы ряда популярнейших тогда романсов. Однажды на спектакле «Лес», 
        поставленном В. Мейерхольдом, прислушиваясь к музыкальному оформлению, 
        В. Кручинин услышал мелодию вальса С. Бейлейзона «Две собачки». Мелодия 
        настолько его увлекла, что он, изменив в ней некоторые интонации и сместив 
        акценты, предложил своему другу поэту Павлу Герману написать песню на 
        тему советской действительности. Так появилась первая советская песня, 
        получившая название «Кирпичный завод». Впервые она была исполнена в одном 
        из спектаклей популярного в Москве театра «Павлиний хвост». Песня имела 
        ошеломляющий успех, ее запели буквально повсюду, а режиссеры Л. Оболенский 
        и М. Доллер даже сняли полнометражный кинофильм на тему этой песни с великой 
        М. Блюмменталь-Тамариной в главной роли. <br>
        <br>
        Случай с песней «Кирпичики» (под таким названием мы знаем ее сегодня) 
        ясно показал, насколько велика была потребность в песнях «нового быта». 
        Нужны были песни, отражающие злободневную реальную повседневную действительность, 
        а не переживания японской гейшы, брошенной бессердечным штурманом английской 
        канонерки Биллом. Поэтому, несмотря на яростное сопротивление РАПМовцев*, 
        посчитавших песню пошлой и мещанской и обрушивших на «Кирпичики» всю мощь 
        своего якобы «пролетарского» гнева, многие композиторы и поэты энергично 
        взялись за разработку новой темы. И вскоре одна за другой стали появляться 
        песни, в различных вариантах повторявшие идею «Кирпичиков» — о том, как 
        простой парень (или девушка) из самых низов, благодаря социально-экономическим 
        преобразованиям в стране, начали жить по-новому: <br>
        <br>
        Муж теперь глава в Совете, <br>
        Электричество завел, <br>
        Петя с Машей, наши дети, <br>
        Записались в Комсомол. <br>
