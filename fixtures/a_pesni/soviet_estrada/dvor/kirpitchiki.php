        Окрыленные успехом, Кручинин и Герман создали еще несколько аналогичных 
        песен (<a href="../drugije/manposelok.htm">&quot;Манькин поселок&quot;</a>, 
        <a href="../drugije/chahta3.htm">&quot;Шахта № 3&quot;</a>). Официальной 
        поддержки жанр не получил, Ассоциация пролетарских музыкантов разразилась 
        очередной критикой, но песни были популярны повсюду. &quot;Песня о кирпичном заводе&quot; 
        и &quot;Шахта №3&quot; были одними из первых в репертуаре Клавдии Шульженко, 
        тогда только начинавшей свою эстрадную карьеру в Харькове. &quot;Кирпичики&quot; 
        вызвали множество фольклорных переработок.<br>
        <br>
        Вместе с тем, песни &quot;нового быта&quot; были привязаны к эпохе и ушли вместе с ней, тогда 
        как &quot;вневременные&quot; экзотические песенки: <a href="vkejptaunskom.php">&quot;В 
        Кейптаунском порту&quot;</a>, <a href="vnachugavan.php">&quot;В нашу гавань 
        заходили корабли&quot;</a>, - а также нэпманские <a href="bubliki.php">&quot;Бублики&quot;</a>, 
        <a href="sodesskogo.php">&quot;С одесского кичмана&quot;</a> и прочие 
         воскресают, как только общество начинает 
        дышать более свободно.<br>
