	<span class="header"><strong>МИЧМАН 
ДЖОН<br></strong></span>
<br>
Сквозь ночной туман<br>
Мрачен океан.<br>
Мичман Джон угрюм и озабочен.<br>
Получил приказ<br>
Прибыть через час.<br>
Мичман Джон не может быть неточен.<br>
И, сжимая руль,<br>
Прямо в Ливерпуль<br>
Он ведет корабль рукою твердой.<br>
Вглядываясь в мрак,<br>
Закурил моряк,<br>
Вдаль глядит уверенно и гордо.<br>
<br>
        <em>Припев:</em><br>
<br>
Еще немного,<br>
Держи на борт,<br>
Ясна дорога,<br>
И виден порт.<br>
Ты будешь первым.<br>
Не сядь на мель!<br>
Чем крепче нервы,<br>
Тем ближе цель.<br>
<br>
Так летят они,<br>
Погасив огни,<br>
Рассекая мощную пучину.<br>
Вдруг ужасный гул<br>
Все судно тряхнул:<br>
&quot;Истребитель&quot; наскочил на мину.<br>
Шум, и гам, и крик.<br>
Джон в единый миг<br>
Нагрузил он шлюпку экипажем.<br>
И, блюдя закон,<br>
Сам последний он<br>
Вплавь пустился, напевая даже.<br>
<br>
Бешеной волной<br>
На берег крутой<br>
Мичман Джон был выброшен под утро.<br>
Там нашла его<br>
Кэт из Чикаго,<br>
Та, чьи зубки блещут перламутром.<br>
Глядя ей в глаза,<br>
Мичман Джон сказал:<br>
- Кэтти, я люблю вас, словно море!<br>
И ему в ответ<br>
Улыбнулась Кэт:<br>
- Это все есть в брачном договоре!<br>
