1. Мичман Джон с &quot;Истребителя&quot;</strong><br>
<br>
Мрачен океан сквозь ночной туман.<br>
Мичман Джон угрюм и озабочен.<br>
Получив приказ прибыть через час, -<br>
Мичман Джон не может быть неточен.<br>
<br>
И, сжимая руль, прямо в Ливерпуль<br>
Он ведет корабль рукою твердой.<br>
Вглядываясь в мрак, закурил моряк,<br>
Вдаль глядит уверенно и гордо.<br>
<br>
Еще немного -<br>
Держи на борт,<br>
Ясна дорога,<br>
И виден порт.<br>
Ты будешь первым!<br>
Не сядь на мель...<br>
Чем крепче нервы,<br>
Тем ближе цель.<br>
<br>
Так спешат они, погасив огни,<br>
Рассекая мощную пучину.<br>
Вдруг ужасный гул судно все встряхнул -<br>
&quot;Истребитель&quot; наскочил на мину!<br>
<br>
Шум, и гам, и крик... Джон в единый миг,<br>
Загрузивши шлюпку экипажем,<br>
Свято чтя закон - сам последний он<br>
Вплавь пустился, напевая даже.<br>
<br>
Бешеной волной на берег крутой<br>
Мичман Джон был выброшен под утро.<br>
Там нашла его Кэт из Чикаго,<br>
Та, чьи зубки блещут перламутром.<br>
<br>
Глядя ей в глаза, мичман Джон сказал:<br>
- Кэтти, я люблю вас, словно море!<br>
И ему в ответ улыбнулась Кэт:<br>
- Это все есть в брачном договоре...<br>
