  Борис Емельянов<br>
  <br>
 Море Белое — водная ширь,<br>
	  Соловецкий былой монастырь.<br>
	  И со всех концов русской земли<br>
	  Нас любовно сюда привезли.<br>
  <br>
  <em>Припев:</em><br>
  <br>
  Всех, кто наградил нас Соловками, <br>
  Просим, приезжайте сюда сами, <br>
  Посидите здесь годочков три иль пять — <br>
  Будете с восторгом вспоминать.<br>
  <br>Соблюдая кодекс трудовой, <br>
	  Охраняет нас милый конвой.<br>
	  А зимой стерегут от беды<br>
	  Целых полгода крепкие льды.<br>
  <br>Хороши по весне комары, <br>
	  Чуден вид от Секирной горы.<br>
	  И от разных ударных работ<br>
	  Здоровеет веселый народ.<br>
  <br>Привезли нам надежд полный куль<br>
	  Бокий, Фельдман, Филиппов и Вуль.<br>
	  А назад повезет Катанян<br>
	  Только грустный напев соловчан.<br>
  <br>Так живем мы, не зная тоски, <br>
	  Благовонной покушав трески.<br>
	  И, вернувшись в родительский дом, <br>
	  С умилением тихо споем.<br>
  <br>
	<em>Журнал &quot;Соловецкие острова&quot;, 
	  Соловки, 1926.</em><br>
<br>
  <br>	
  Песня Соловецких лагерей. В тексте упомянуты фамилии высшего начальства ГУЛАГа (Бокий, Фельдман и т.д.). Имя <em>&quot;Глеб Бокий&quot;</em> носил также пароход, 
  связывавший Соловецкие лагеря с материком (бывший пароход &quot;Соловецкий&quot;, 
  ранее перевозивший паломников). <br>
  <br>
Академик Дмитрий Лихачев, вспоминая о неофициальном гимне Соловецких лагерей &quot;Соловецкие огоньки&quot;, приводит припев и один из куплетов выше приведенной песни вместе с другими, которые в сборнике &quot;Песни узников&quot; (Сост. Владимир Пентюхов. Красноярск, 1995) включены в песню  <a href="sologonki.php">&quot;Занесет нас зимою метель&quot;</a>, также с указанием на авторство Бориса Емельянова - поэта из московской золотой молодежи эпохи НЭПа, до отправки на Соловки выступавшего в кабаре с экспромтами. <br>
  <br>
   Емельянову в сборнике &quot;Песни узников&quot; приписываются также песня <a href="vtomkraju.php">&quot;Секирная гора</a>&quot; и более поздние песни -  <a href="nepetchalsia.php">&quot;Не печалься, любимая&quot;</a> об этапе в Колымские лагеря, и возникшая не ранее середины 1930-х гг. <a href="bryzgi.php">"Брызги шампанского"</a>.<br>
  <br>
  Журнал &quot;Соловецкие острова&quot; (первоначальное название - &quot;СЛОН&quot;) выпускался в 1924-1926 и 1929-1930 гг.  силами заключенных, он свободно распространялся 
  в СССР в розницу и по подписке. <br>
  <br>
  <br>
  <span class='bold'>Соловецкие лагеря особого назначения</span> (СЛОН) основаны весной 
  1923 г. на Соловецких островах в строениях бывшего монастыря, переданных в 1922 
  г. ГПУ. До 1929 г. были единственными концлагерями в СССР. Осенью 1931 г. их заключенные 
  составили первый десант строителей Беломорканала. В 1936 г. переименованы 
  в СТОН (Соловецкая тюрьма особого назначения). В 1939 г. упразднены. <br>
