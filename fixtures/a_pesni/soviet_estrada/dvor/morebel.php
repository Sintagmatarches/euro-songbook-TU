  <strong>Из воспоминаний академика Дмитрия Лихачева (<a href="http://www.sakharov-center.ru/asfcd/auth/?t=book&amp;num=325" target="_blank">Лихачев Д. С. Воспоминания. СПб.: Logos, 1995</a>), глава &quot;Солтеатр&quot;:</strong><br>
  <br>
Было на Соловках и другое «чекистское чудо»: Соловецкий театр (Солтеатр), созданный для «туфты» — изображать культурно-воспитательную работу, но ставший немаловажной реальностью соловецкой интеллектуальной жизни. Наряду с «живгазетой», концертными номерами» самого низкого пошиба, там шла и интересная творческая работа.<br>
<br>
В годы моего пребывания на Соловках душой Солтеатра, как и журнала «Соловки», был Борис Глубоковский — актер Камерного театра Таирова, сын известного в свое время богослова и историка церкви Николая Никаноровича Глубоковского, переписка которого с В. В. Розановым не так давно опубликована.<br>
<br>
Бориса Николаевича Глубоковского я хорошо знал, но не как близкого знакомого, а как чрезвычайно видную и много сделавшую для лагерной интеллигенции личность. Его, по существу, знали все. Жаль, что не сохранилось его фотографии. Это был высокого роста человек, стройный, красивый, живой, с хорошими манерами. Одет он был по соловецкой моде немногих людей, которым был доступен Помоф (пошивочная мастерская, одевавшая жен немногих вольнонаемных и наиболее блатных из заключенных): черное полупальто с кушаком, черные галифе, высокие сапоги, кепка чуть набекрень.<br>
<br>
Он был разносторонне одарен. Ему приписывалось участие в богемном окружении Есенина. Обвинялся за участие в каком-то заговоре «Белого центра». Обвиняться он, конечно, мог, но вряд ли бы он стал рисковать по свойствам своей несколько эгоистической натуры.<br>
<br>
Солтеатр был главным «показушным» предприятием на Соловках. Театром хвастались перед различными комиссиями, перед приезжавшим из Москвы начальством, перед Горьким, побывавшем на Соловках весной 1929 г.<br>
<br>
Вот некоторые постановки в Солтеатре: «Дети Ванюшина», «Блоха» Е. Замятина, «Маскарад» М. Лермонтова. Полный репертуар Солтеатра можно восстановить по ж. «Соловецкие острова» и газете «Новые Соловки», а также по маленькой газетке «Соловецкий листок», издававшейся тогда, когда Управление СЛОН в 1930 г. переехало в Кемь, а вместе с Управлением переехал туда же и Глубоковский.<br>
<br>
Он написал много статей и одну книгу — «49», изданную Соловецким обществом краеведения году в 1926-м или 1927-м — об уголовниках, попавших в лагерь по статье 49 уголовного кодекса о «социально опасных».<br>
<br>
Чрезвычайной популярностью пользовалась на Соловках его постановка «Соловецкое обозрение». Постановка остро иронизировала над соловецкими порядками, бытом и даже начальством. Однажды, когда Одна из «разгрузочных комиссий» в подпитии смотрела в театре «Соловецкое обозрение», в переполненном заключенными зале, Б. Глубоковский (тоже, очевидно, хлебнувший), который вел представление, выкрикнул со сцены: «Пойте так, чтобы этим сволочам (и он указал рукой на комиссию) тошно было». А обозрение состояло не только из комических номеров, но и тоскливо-лирических, заставлявших многих плакать. Стихи писал сам Глубоковский (что мог, я записал), а мотивы он подбирал главным образом из оперетт. Но все ж таки один мотив сочинил, говорят, сам: к его песне «Огоньки», которую в начале 30-х гг. пела вся Россия. Заканчивалась эта песнь следующими словами:<br>
<br>
От морозных метелей и вьюг<br>
Мы, как чайки, умчимся на юг,<br>
И мелькнут вдалеке огоньки — Соловки, Соловки, Соловки.<br>
<br>
Припев был такой:<br>
<br>
Обещали подарков нам куль<br>
Бокий, Фельдман, Васильев и Вуль.<br>
Но в Москву увозил Катанян<br>
Лишь унылый напев соловчан:<br>
<br>
Всех, кто наградил нас Соловками,<br>
Просим, приезжайте сюда сами.<br>
Посидите здесь годочков три иль пять —<br>
Будете с восторгом вспоминать.<br>
<br>
Среди куплетов был и такой, обрисовывавший представления о будущем заключенных:<br>
<br>
И когда-нибудь вьюжной зимой<br>
Мы сберемся веселой толпой,<br>
И начнут вспоминать старики<br>
Соловки, Соловки, Соловки!<br>
<br>
Наивные мечты заключенных двадцатых годов…<br>
<br>
В Солтеатре были и другие постановки. Я помню «Маскарад» Лермонтова. Арбенина играл Калугин — артист Александрийского театра в Петрограде. Дублировал Калугина Иван Яковлевич Комиссаров, — король всех урок Соловецкого архипелага. В прошлом бандит, ходивший «на дело» во главе банды с собственным пулеметом, грабивший подпольные валютные биржи, ученик и сподвижник знаменитого Леньки Пантелеева. А Арбенин у него был настоящим барином...<br>
<br>
Что еще шло в Солтеатре, не помню. Были и киносеансы. Помню фильм по сценарию Виктора Шкловского, где двигались какие-то броневики через Троицкий мост в Петрограде. Ветер нес бумаги, мусор. Были и какие-то концерты, на которых актеры из урок ловко отбивали чечетку, показывали акробатические номера (особенным успехом пользовалась пара — Савченко и Энгельфельд). Оркестром дирижировал Вальгардт — близорукий дирижер из немцев, впоследствии дирижировавший оркестром в Одессе и еще где-то (сидел он по делу кружка А. А. Мейера). Была актриса, истерическим голосом читавшая «Двенадцать» Блока. Была хорошенькая певица Перевезенцева, певшая романсы на слова Есенина (помню — «Никогда я не был на Босфоре...») и нещадно изменявшая мужу, работавшему в Кремле и пытавшемуся из ревности покончить с собой в одной из рот. В фойе театра читались лекции по истории музыки профессором-армянином из Тифлиса В. Анановым, по психологии А. П. Суховым и еще кем-то и о чем-то.<br>
<br>
У меня сохранилась афиша Вечера памяти Н. А. Некрасова в Солтеатре в четверг 12 января 1929 г. Я на нем не был: лежал больной тифом. Открывал вечер докладом Б. М. Лобач-Жученко. Это была заметная личность в лагерной жизни, но я его, к сожалению, совсем не помню. Ощущение чего-то большого и значительного, которое у меня возникает при упоминании его фамилии, может быть, вызвано самой его фамилией — длинной и какой-то важной<sup>1</sup>. Затем следовали доклады Б. Глубоковского, П. И. Иогалевича, П. С. Калинина, Я. Я. Некрасова (Некрасова я встречал на Беломоробалтийском строительстве, но там был другой — один из глав Временного правительства, масон). После антракта следовал концерт, в котором принимали участие чтецы (была и хоровая декламация, — модная в те времена), духовой оркестр, симфонический квинтет, соловецкий хор. Самое интересное, что исполнялись отдельные части оперы «Кобзарь», сочиненной заключенным Кенель. Как и все представления в Солтеатре, начало было поздно — в 9 часов, так как официальный конец работы в лагере был в 8 часов вечера. Программка открывается неплохим портретом Н. А. Некрасова — гравюрой по линолеуму заключенного И. Недрита.<br>
<br>
И все это в разгар тифозной эпидемии и истязаний на общих работах! Воистину «Остров чудес».<br>
<br>
«Все смешалось здесь без цвета и лица» (из соловецкой песни Глубоковского на мотив из «Жрицы огня»).<br>
<br>
Б. Н. Глубоковский по освобождении из Белбалтлага получил удостоверение (как и многие из нас) с красной диагональной полосой. По этому удостоверению его прописали в Москве и приняли назад в Камерный театр. Как я узнал из объявления в газете, умер он в середине 30-х гг. Говорили — от заражения крови. Он стал морфинистом и кололся прямо через брюки...<br>
<br>
Зимой 1929—1930 г., когда свирепствовал «второй тиф», так называемый «азиатский», люди заболевали тысячами, театр был закрыт и зрительный зал обращен в лазарет, где больные лежали вповалку, почти без помощи. Но на сцене за спущенным занавесом читались лекции, хотя из зала доносились стоны и крики. Много лекций по музыке прочел уже упомянутый мной Ананов — бывший сотрудник Театра имени Руставели в Тифлисе и газеты «Заря Востока». Прочел и я что-то о дошекспировском театре (упросили, хотя я и понимал всю нелепость такой лекции в таких условиях, но для КВЧ (Культурно-воспитательной части) моя лекция была нужна «для галочки»). Весной, когда тиф прекратился и из оркестровой ямы стали доставать сваленные туда скамьи и стулья, нашли труп умершего. Он был до того худ, что высох и не очень пропах. Больные расползались в бреду. Под аккомпанемент стонов была еще чья-то лекция о театре масок. А привезенный прямо из поездки за границу корреспондент Гарри Бромберг в широчайших модных тогда брюках — «оксфордах» — и коротеньком пиджаке делился там своими заграничными впечатлениями. Самое место! Мне кажется, что Солтеатр с его занавесом, отделявшим смерть и страдания тифозных больных от попыток сохранить хоть какую-то иллюзию интеллектуальной жизни теми, кто завтра и сам мог оказаться за занавесом, — почти символ нашей лагерной жизни (да и не только лагерной — всей жизни в сталинское время).<br>
<br>
Вокруг и внутри театра шли обычные для театров интриги. Образовывались какие-то группки, сторонники тех или иных актеров и постановщиков. Помню, что в еженедельной газете «Соловецкий листок», ставшей выходить после перевода «Новых Соловков» на материк, я поместил в 1930-м или 1931 г. какую-то похвальную рецензию на одну из постановок Солтеатра. Немедленно на следующей неделе появился ответ мне: «Рецензия по блату», хотя, ей-ей, я писал, как стремлюсь всегда, искренне.<br>
<br>
Жизнь на Соловках в 1929—1931 гг., возможно, покажется читателю «театром абсурда»: богатство интеллектуального общения в условиях лагеря со всеми его атрибутами — чекистами, камерами, карцерами.<br>
<br>
Прежде всего следует упомянуть мужской карцер на Секирной горе («Секирка»), женский карцер на Большом Заяцком острове («Зайчики»), Голгофу на о. Анзере для безнадежно больных и глубоко старых людей (главным образом священников и нищих, собранных с папертей московских церквей).<br>
<br>
Существовали лесоразработки, торфоразработки, обширные лагеря в Савватиеве, Исакове, Филимонове, Муксалме.<br>
<br>
Существовали безымянные лагеря в лесу. В одном из них я был и заболел от ужаса увиденного. Людей пригоняли в лес (обычно в лесу были болота и валуны) и заставляли рыть траншею (хорошо, если были лопаты). Две стороны этих траншей, чуть повыше, служили для сна, вроде нар; центральный проход был глубже и обычно весной заполнялся талой водой. Чтобы лечь в такой траншее спать, надо было переступать через уже лежавших. Крышей служили поваленные елки и еловые ветки. Когда я попал в такую траншею, чтобы спасти из нее детей, в ней «шел дождь»: снег наверху уже таял (был март или апрель 1930 г.), сливался и на земляные лежбища, и в центральную канаву, которая должна была служить проходом.<br>
<br>
Я уже не говорю о «комариках» (наказание, применявшееся летом), о том, как не пускали на ночь и в эти траншеи, если не выполнялся «урок», как работали, какой выполняли «ударный» план. После одного такого посещения лесного лагеря у меня открылись сильнейшие язвенные боли, которые вскоре прошли, так как появилось язвенное кровотечение, перенесенное мною «на ногах».<br>
<br>
В этих-то лесах главным образом и погибали заключенные. В 30-м г. осенью умерли тысячи «басмачей» — изнеженных восточных мужчин в халатах и шелковых башмаках. Умерли интеллигенты, которых мы, жившие в Кремле, не успели перехватить из 13-й и 14-й рот...<br>
<br>
Итак, описав кое-как «богатство» соловецкой топографии, перехожу к рассказу о своих «путешествиях» по этому миру, в которых встречи с людьми — главное.&quot;<br>
