  <strong>4. На бульваре Гоголя клены опадают</strong><br>
  <br>
  На бульваре Гоголя клены опадают. <br>
  И всю ночь над городом фонари горят. <br>
  Мальчикам и девочкам по уши влюбленным, <br>
  Дождички и дворнички не дают гулять. <br>
  <br>
  На бульваре Гоголя назначал свидание, <br>
  На скамейке крашеной - пятой от угла. <br>
  В голубые сумерки осенью холодною, <br>
  В восемь без пятнадцати ты меня ждала. <br>
  <br>
  Голубые сумерки осенью холодною <br>
  Ты сказала первая: - Ухожу, прощай!<br>
  Так живи, пожалуйста, с кем тебе угодно <br>
  И кому угодно встречи назначай. <br>
  <br>
  Наши встречи кончились, ты совсем чужая, <br>
  А проходишь мимо - меня не узнаешь. <br>
  Ты одними танцами бредишь, дорогая, <br>
  Песенки вульгарные без конца поешь. <br>
  <br>
  Встретились мы рано, но расстались поздно. <br>
  Видно предназначено нам с тобой судьбой<br>
