  <strong>1. На бульваре Гоголя</strong><br>
  <br>
На бульваре Гоголя клены опадают,<br>
Целый день над городом облака висят.<br>
Паренькам и девушкам, по уши влюбленным,<br>
Дождички и дворники не дают гулять.<br>
<br>
Я напротив Гоголя назначал свидание<br>
На скамейке крашеной, первой от угла.<br>
В голубые сумерки через каждый вечер <br>
В восемь без пятнадцати ты меня ждала.<br>
<br>
В голубые сумерки осенью холодною<br>
Ты сказала первая: &quot;Ухожу, прощай!<br>
Ты гуляй, пожалуйста, с кем тебе угодно<br>
И кому угодно ты свиданья назначай!&quot;<br>
<br>
Наши встречи кончились, ты теперь чужая,<br>
Ты проходишь мимо, меня не узнаешь.<br>
Ты одними танцами бредишь, дорогая,<br>
Песенки ненужные без конца поешь.<br>
<br>
Наши встречи кончились, мы теперь чужие,<br>
Ты уже встречаешься, у тебя – другой,<br>
Так забудь, пожалуйста, станцию Динамо,<br>
Лодочку двухвесельную над Москвой-рекой.<br>
<br>
<em>Две последние строчки куплетов повторяются<br>
<br>
Расшифровка фонограммы Натальи Варлей. CD «В нашу гавань заходили 
  корабли» №1, 2001.</em><br>
<br><br>
  <strong>2. На бульваре Гоголя</strong><br>
  <br>
  На бульваре Гоголя опадают клены,<br>
  Облака над городом день и ночь стоят.<br>
  Мальчикам и девочкам, по уши влюбленным,<br>
  Дворники и дождички не дают житья.<br>
  <br>
  На бульваре Гоголя назначал свидания<br>
  На скамейке крашеной, пятой от угла.<br>
  В голубые сумерки в восемь без пятнадцати<br>
  Через каждый вечер ты меня ждала.<br>
  <br>
  Но однажды вечером осенью холодной<br>
  Ты сказала: - Больше я не приду, прощай!<br>
  Что ж, живи, пожалуйста, как тебе угодно<br>
  И кому угодно встречи назначай.<br>
  <br>
  И душа сжимается, и внутри обида.<br>
  Что же, до свидания и счастливый путь!<br>
  Только уж, пожалуйста, станцию Опалиха<br>
  И дорогу Рижскую навсегда забудь.<br>
  <br>
  <em>В нашу гавань заходили корабли. Пермь: Книга, 1996. 
  Перепечатано: В нашу гавань заходили корабли. Вып. 2. М.: Стрекоза, 2000.</em><br>
 <br>
  <br>
  По словам Успенского, сказанным позднее в одной из телепередач &quot;Гавани&quot; 
  (&quot;5-й канал&quot;, 6 июля 2008 года), эту песню собирали по кусочкам, и название 
  станции не вспомнили. Тогда Успенский вставил станцию Опалиха - она была созвучна 
  названию той станции, которая была, по его мнению, в песне, и которую он вспомнить не смог.<br>
  <br>
  <br>
  <strong>3. На бульваре Гоголя листья опадают... </strong><br>
  <br>
  На бульваре Гоголя листья опадают,<br>
  На бульваре Гоголя облака плывут.<br>
  Мальчикам и девочкам, по уши влюбленным,<br>
  Дождики да дворники покоя не дают. <br>
  <br>
  Парень любит девушку, в ней души не чает,<br>
  Парень любит девушку, но она уйдет.<br>
  Ну а если девушка парня покидает,<br>
  Ей такую песенку паренек споет. <br>
  <br>
  На бульваре Гоголя назначал свидание<br>
  На скамейке крашеной, восемь от угла.<br>
  В голубые сумерки ровно без пятнадцати,<br>
  Ровно без пятнадцати ты меня ждала. <br>
  <br>
  Но однажды осенью, осенью холодной<br>
  Ты сказала первая: &quot;Уходи, прощай!<br>
  Уходи, пожалуйста, куда тебе захочется,<br>
  И кому захочется, свиданья назначай&quot;. <br>
  <br>
  На бульваре Гоголя назначал свидание<br>
  На скамейке крашеной, справа от угла.<br>
  В голубые сумерки осенью холодной<br>
  В восемь без пятнадцати ты меня ждала. <br>
  <br>
  <em>Расшифровка фонограммы в исп. Комара (Александра Спиридонова), 
  начало 1970-х гг.</em><br>
 <br>
  <br>
  <img src="komar.jpg" alt="Комар (Александр Спиридонов, 1948-1996)" width="380" height="488"><br>
  <br>
  <strong>Александр Спиридонов</strong> (&quot;Комар&quot;, 1948, Воронеж - 1996, 
  там же) - подпольный воронежский исполнитель, учился в музыкальной школе на 
  баяне, гитару освоил у цыган. Записывался на подручных средствах со 2-й п. 
  1960-х. В 1968 получил первый срок за конфликт с милицией, следом - второй за 
  нарушение режима надзора после освобождения, затем было еще несколько судимостей. 
  Последний раз освободился в 1984. Женился второй раз, трое детей. После смерти 
  жены в 1992 впал в запой, записывался мало. Погиб случайно - забит 
  до смерти и ограблен по пути в ларек. <br>
