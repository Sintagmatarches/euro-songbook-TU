  <br>
  Песня, о которой пойдет речь, едва появившись на свет, тут же «покинула родителей» 
  и ушла «в люди». Лихой мотивчик и злободневный для всех времен текст зазвучал 
  в репертуаре Аркадия Северного и Саши Комара еще в семидесятые, а в недавнем 
  прошлом композицию записали в своих альбомах Владислав Медяник и Леонид Коржов. 
  Настоящий хит, она лишь самую малость проигрывает в популярности «Мурке» или 
  «Лимончикам». Да и то, скорее, из-за своей «молодости». Как ни странно, но знаменитая 
  «Кралечка» вышла из-под пера талантливых авторов всего четыре десятка лет назад, 
  и не во времена Бени Крика, как кому-то может показаться. <br>
  <br>
  В начале шестидесятых годов на Одесской киностудии снимался фильм «Страницы 
  былого». По сюжету в картине должны были прозвучать три песни, сочинением которых 
  занимались известный композитор Андрей Эшпай и поэт Владимир Карпеко. <br>
  <br>
  Две композиции, воспевающие в духе тех времен «комсомольцев-добровольцев», сложились 
  легко, а вот с третьей, которую по сценарию исполнял хулиган Яшка Пятачок, авторам 
  пришлось намучиться. Никак не выходило у морально устойчивых советских художников 
  изобразить «приблатненную» музыкальную зарисовку. <br>
  <br>
  Режиссер забраковал уже полдюжины вариантов, но все впустую. Творческий тандем 
  забуксовал. <br>
  <br>
  Теплым летним вечерком расстроенные соавторы решили немного развеяться и прогуляться 
  по «красавице Одессе». Задумчивые компаньоны не спеша бродили по улочкам и оказались 
  в небольшом скверике. Дни стояли погожие, парни криминального вида, в кепочках-восьмиклинках, 
  тельняшках и со сверкающими рандолевыми фиксами, отчаянно резались в карты. 
  Один из игроков — смазливый малый с острым взглядам и непослушной челочкой — 
  в запале борьбы вдруг яростно выругался: «Чтоб тебя! Опять кралечка вразрез!» 
  <br>
  <br>
  Этим жаргонным выражением картежники называют ситуацию, когда дама при раздаче 
  оказывается между двумя тузами. <br>
  <br>
  Не дожидаясь ответа его партнеров, поэт уже тянул коллегу за рукав в сторону 
  гостиницы. <br>
  <br>
  — Скорее! Скорее! Песня готова! Надо только успеть записать! — бормотал вдохновленный 
  автор. <br>
  <br>
  Реплика неизвестного одессита помогла. за какой-то час родилось произведение, 
  над которым Карпеко и Эшпай безуспешно бились неделями. <br>
  <br>
  Новинку продемонстрировали peжиссеру — он остался доволен. Теперь можно было 
  снимать задуманный эпизод. Мотор! Камерa! Дубль три! И в кадре появляется, идущий 
  «походкой пеликана» хулиган Яшка, за которым столь же вальяжно передвигаются 
  два дружка-гитариста. Пятачок при этом строил глазки встречным барышням и весело 
  распевал: <br>
