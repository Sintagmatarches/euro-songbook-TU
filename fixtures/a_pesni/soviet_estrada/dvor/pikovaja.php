  Успех картины превзошел все ожидания, и «Кралечку» стал распевать весь криминальный 
  мир Одессы-мамы. Правда, последний куплет они почему-то игнорировали. Видимо, 
  «отсутствие монет» не пришлось уркаганам по вкусу: они сами привыкли «вытряхивать» 
  их из граждан, а тут стать жертвой какой- то «девочки» — это, извините, не по 
  понятиям. <br>
  <br>
  Спустя год новый заказ с киностудии вновь привел Карпеко в прекрасный южный 
  город. Как-то раз, задержавшись на съемках, он поздним вечерам спешил в гостиницу. 
  Внезапно дорогу преградили трое амбалов: <br>
  <br>
  — Дядя! Пиджачок не жмет? А часы не мешают? Мы-таки избавим вас от этих хлопот! 
  Скидывай все сюда! Позвольте поухаживать! — с характерным одесским акцентам 
  произнес главарь. <br>
  <br>
  Карпеко, в недавнем прошлом фронтовик, не растерялся: <br>
  <br>
  — Ребята, меня нельзя раздевать. Я ваш гимн написал. <br>
  <br>
