  Два туза, а между –<br>
  Дамочка вразрез… и т. д.<br>
  <br>
  (1) В ряде вариантов (в том числе у Аркадия Северного) — «Раз гляжу я между 
  кралечке в разрез», т. е. речь идёт о подсматривании женщине в разрез юбки, 
  между ног. Это, конечно, искажённое понимание текста. В авторском варианте имеется 
  в виду ситуация в карточной игре «очко» («двадцать одно»), где выигрывает тот, 
  кто набирает 21 очко или опережает партнёра по очкам, но не перебирает в сумме 
  21. При этом абсолютным выигрышем, помимо 21, считается выпадение двух тузов 
  — «золотое очко». Но часто между тузами проскакивает любая другая карта - так 
  называемая «прокладка», и игрок, разумеется, перебирает, проигрывает. Эта ситуация 
  и описана в куплете, а в качестве «прокладки» выступает пиковая дама — видимо, 
  автор памятует о Пушкине.<br>
  (2) Вариант:<br>
  «Впрочем, извините:<br>
  Где ваш кошелёк?»<br>
  (3) Вариант – «Бар шумит давно», «Бан шумит всю ночь» и проч.<br>
  (4) Баландец – (иронич.) баланда, лагерная похлёбка.<br>
  (5) Шмон – обыск.<br>
  <br>
  Песня чрезвычайно популярна среди так называемых «блатных» бардов. Многие её 
  считают «старой арестантской». Между тем она возникла в начале 60-х годов; начальный 
  вариант принадлежит перу поэта Игоря Шаферана и создавался для приключенческого 
  фильма Одесской киностудии. Очень удачная стилизация. Правда, что касается игры 
  в «очко», упоминаемой в песне, надо заметить: среди «порядочных», «честных воров», 
  «бродяг» она считалась «недостойной». «Истинно воровские» карточные игры — стос, 
  терц, бура, отчасти — рамс.<br>
  <br>
  Приводимый вариант отличается от текста Шаферана, поскольку можно сказать, что 
  песня стала уголовным фольклором и обросла вариациями и дополнительными куплетами. 
  <br>
  <br>
  <em>Жиганец Ф. Блатная лирика. Сборник. Ростов-на-Дону: «Феникс», 
  2001, с. 162-163.</em><br>
 <br>
  <br>
  Песня действительно написана для фильма Евгения Ташкова «Страницы былого», повествующего 
  об одесских марксистах-подпольщиках 1901 года и снятого на Одесской киностудии. Но только не в в начале 1960-х, а в 1957 году, а автором стихов песен фильма, 
  в том числе и этой песни (авторское ее название - &quot;Кралечка вразрез&quot;), 
  был не Игорь Шаферан, а Владимир Карпеко. Автор музыки - Андрей Эшпай. Из фильма 
  песня шагнула в народ.<br>
