  <br>
  — Нет. «Кралечка вразрез». <br>
  <br>
  — Каму фуфло толкаешь, шляпа?! «Кралечку» он написал! А Гимн Советского Союза 
  тоже ты написал? <br>
  <br>
  — Так я докажу, — пошел ва-банк автор. — Вы знаете два куплета, а в песне их 
  три. <br>
  <br>
  И, не дожидаясь вопросов, исполнил заключительное четверостишье. <br>
  <br>
  Громилы поверили сразу. Не может же человек с ходу взять и сочинить недостающие 
  строчки. Значит, не врет дядя. <br>
  <br>
  — Ладно, мужик, пойдешь с нами, — приказал старший. <br>
  <br>
  Поэт уже не боялся хулиганов, ему стало любопытно, чем же закончится история. 
  <br>
  <br>
  Поплутав по темным дворикам, компания оказалась на богатой малине. Длинный стол, 
  застеленный сияющей альпийским снегам белой скатертью, лучшие блюда и напитки 
  перед гостями, коих поэт насчитал полтора десятка. Во главе сборища восседал 
  симпатичный мужчина лет тридцати в элегантном костюме. На лацкане пиджака, к 
  своему немаламу удивлению, Владимир Карпеко разглядел университетский значок. 
  <br>
  <br>
