  <br>
  — Коська, мы привели тебе человека, который написал нашу песню. <br>
  <br>
  — Пусть исполнит, — барственно кивнув, приказал молодой человек. Видимо, жиган 
  остался доволен и песней, и ее автором. Далеко за полночь, насладившись культурным 
  обществам, он распорядился проводить дорогого гостя до гостиницы. Прощаясь, 
