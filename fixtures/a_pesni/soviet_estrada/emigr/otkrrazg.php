Чем ворчать и омрачать свиданье,<br>
Улыбнись и выслушай меня…<br>
Человек без нового заданья<br>
Все равно что печка без огня!<br>
<br>
И давай запомним, друг мой милый:<br>
Нынче мало Родину любить,<br>
