Слова Булата Окуджавы<br>
<br>
1. Не сольются никогда зимы долгие и лета: <br>
У них разные привычки и совсем несхожий вид. <br>
Не случайны на земле две дороги — та и эта. <br>
Та — натруживает ноги, эта — душу бередит. <br>
<br>
<em>Припев:</em><br>
<br>
Эта женщина в окне, в платье розового цвета, <br>
Утверждает, что в разлуке невозможно жить без слез, <br>
Потому что перед ней две дороги — та и эта. <br>
Та прекрасна, но напрасна, эта, видимо, всерьез. <br>
<br>
2. Хоть разбейся, хоть умри, не найти верней ответа. <br>
И куда бы наши страсти нас с тобой ни завели, <br>
Неизменно впереди две дороги — та и эта, <br>
Без которых невозможно, как без неба и земли. <br>
