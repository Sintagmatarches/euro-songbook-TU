<h1>ПЕСНЯ О СЧАСТЬЕ</h1>


Музыка Бориса Фомина<br>
Слова Людмилы Давидович<br>
<br>
Как часто мы, того не замечая,<br>
Стремимся вдаль за новою мечтой.<br>
Вчерашний день нам кажется случайным,<br>
А счастье принесет совсем другой.<br>
<br>
Каждому хочется счастье найти:<br>
Любить и быть любимым.<br>
Счастье лежит у нас на пути,<br>
А мы проходим мимо.<br>
<br>
Шел крупный снег, и вечер был холодный,<br>
Со мной был друг, любимый и родной,<br>
Но мне казалось: это несерьезно,<br>
А счастье принесет совсем другой.<br>
<br>
Каждому хочется счастье найти…<br>
<br>
Но все прошло, и я его забыла,<br>
Прошли года, и я вдруг поняла,<br>
Что это счастье настоящим было,<br>
А я чего-то нового ждала.<br>
<br>
Каждому хочется счастье найти…<br>
