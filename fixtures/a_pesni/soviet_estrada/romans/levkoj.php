<h1>ЛЕВКОЙ</h1>
Музыка Петра Фурмана<br>
Слова Бориса Тимофеева<br>
<br>
Ночь пришла так скоро,<br>
Стало вдруг темно.<br>
Лишь немым укором<br>
Луч луны глядит в окно.<br>
Вновь воспоминанья жгут сердце огнем,<br>
И поет рояль опять о нем.<br>
<br>
Чуть белеют левкои<br>
В голубом хрустале.<br>
Мир в безмолвном покое,<br>
Ночь прошла по земле.<br>
<br>
Лишь под бледной рукою<br>
Тихо клавиши плачут,<br>
Нежно сердце печаля<br>
В синей праздничной мгле.<br>
<br>
С этим милым мотивом<br>
Столько радости слито.<br>
Ты любил эту песню<br>
И меня ты любил.<br>
<br>
Но от песни счастливой<br>
Тихо клавиши плачут.<br>
И мелодию эту,<br>
Как меня, ты забыл.<br>
<br>
