  <br>
  Романс написан в Тифлисе Борисом Прозоровским для Тамары Церетели вскоре после 
  их знакомства там (т.е. между 1920 и осенью 1923 года, когда оба перебрались в Москву). 
  Как и герои романса, Прозоровский и Церетели &quot;странно встретились&quot; 
  и в дальнейшем еще более &quot;странно разошлись&quot;. Познакомились в 1920 
  году в Тифлисе: она, студентка Тифлисской консерватории, впервые выходит на 
  сцену, он, петербуржец, после революции 1917 года уехавший в Тифлис, работает пианистом 
  и аранжировщиком и сочиняет романсы. В Тифлисе кипела концертная жизнь, а вокруг 
  в России шла Гражданская война. Церетели и Прозоровского разлучила лишь смерть 
  - в 1937 году композитор был расстрелян. Церетели продолжала петь до 1960 года, затем 
  ушла с эстрады.<br>
