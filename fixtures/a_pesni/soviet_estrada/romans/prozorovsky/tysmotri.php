  <br>
  <em>«Уполномоченному репертуарного комитета от Изабеллы Юрьевой.<br>
  <br>
  Заявление<br>
  Прошу разрешить мне для исполнения в новой программе Мюзик-холла следующие старинные 
  романсы: «Никому ничего не рассказывай», «Жигули», «Средь миров», «Он уехал».<br>
  <br>
  И. Юрьева»<br>
