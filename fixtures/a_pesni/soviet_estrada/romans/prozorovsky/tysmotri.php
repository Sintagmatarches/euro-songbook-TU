<br>
  <br>
  17 апреля 1933 года досрочно освобожденный с Беломорканала Борис Прозоровский 
  и Тамара Церетели записали на грампластинки романсы и песни «Не на сердце кольцо», 
  «Жигули», «Никому ничего не рассказывай» и «Я старше вас» (последний - авторства 
  Бориса Фомина). Эти записи Бориса Прозоровского оказались последними в его жизни. 
  Вскоре он был снова арестован за дворянское происхождение и в ноябре 1937 года расстрелян. Михаила Козырева, поэта и писатель-сатирика, арестовали в июле 1941 года по обвинению в антисоветской и антипартийной агитации, в том числе за распространение среди знакомых неопубликованной повести 1924 года &quot;Ленинград&quot;. В начале 1942 года его расстреляли в Саратовской тюрьме.<br>
