  <br>
  Романс написан Борисом Прозоровским в 1924 году в Москве. Встречается также 
  под заглавиями &quot;Газовая косынка&quot;, &quot;Ты смотри, никому не рассказывай&quot;, 
  &quot;Никому ничего не рассказывай&quot;, &quot;Никому не рассказывай&quot;. 
  Иногда слова тоже приписываются Прозоровскому. Часто публикуется как произведение 
  неизвестных авторов, так как оба реальных автора были репрессированы, и романс с конца 1930-х годов вплоть до &quot;оттепели&quot; мог бытовать лишь под видом анонимного.<br>
