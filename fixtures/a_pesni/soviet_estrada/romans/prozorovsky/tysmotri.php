  Резолюция: «Исключительный по пошлости и мещанству репертуар. Разрешить сроком 
  на один год одной певице, пока не будут подготовлены произведения, созвучные 
  времени.<br>
  <br>
  Редактор Пикель. 1927 год, июнь».</em><em><br>
