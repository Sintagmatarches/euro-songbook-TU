<h1>ПЕРВЫЙ, 
ВТОРОЙ…</h1>
<br>
В полку воздушном есть отряд, <br>
В нем служат шестеро ребят. <br>
И знает весь аэродром - <br>
Они повсюду вшестером. <br>
Когда гулял над полем ветер, <br>
Друзья кидались смело в бой: <br>
Первый, второй, третий, <br>
Четвертый, пятый и шестой. <br>
<br>
На славном жизненном пути <br>
Ты встретишь их всегда в шести, <br>
И коль один захочет спать, <br>
За ним зевают сразу пять. <br>
Один проснется на рассвете, <br>
И просыпаются гурьбой <br>
Первый, второй, третий, <br>
Четвертый, пятый и шестой.<br>
<br>
Один влюбился наконец, <br>
За ним - все пятеро сердец. <br>
И на свиданье вечерком <br>
Друзья ходили вшестером. <br>
&quot;Я вас люблю&quot;, - при лунном свете <br>
Шептали девушке одной <br>
Первый, второй, третий, <br>
Четвертый, пятый и шестой. <br>
<br>
Но лучше всех среди шести <br>
Не может девушка найти. <br>
И потому она в ответ <br>
Всем шестерым сказала: &quot;Нет&quot;. <br>
Сраженные ответом этим, <br>
Всю ночь бродили под луной <br>
Первый, второй, третий, <br>
Четвертый, пятый и шестой. <br>
<br>
Но как-то раз друзьям пришлось <br>
По парку прогуляться врозь. <br>
И встретили они все вдруг <br>
Шесть чудных девушек-подруг. <br>
И все влюбились, словно дети, <br>
И запылали всей душой <br>
Первый, второй, третий, <br>
Четвертый, пятый и шестой. <br>
<br>
В полку воздушном есть отрад, <br>
В нем каждый счастлив и женат. <br>
И ждут шесть жен и шесть мужей <br>
К весне рожденья малышей. <br>
И вот, друзья, на белом свете <br>
В семье прибавится большой <br>
Первый, второй, третий, <br>
Четвертый, пятый и шестой. <br>
