	<h1>БЕЛАЯ 
НЕВЕСТА </h1>
<br>
На могилу парня белая невеста<br>
Принесла со свадьбы белые цветы.<br>
Он со службы срочной обещал вернуться,<br>
Говорил, моей женою будешь только ты.<br>
<br>
Он служил далеко, доблестно и честно,<br>
Был хорошим парнем, верен был судьбе.<br>
Но подвел девчонку, не пришел со службы,<br>
А домой вернулся в цинковом гробу.<br>
<br>
Так она стояла на плите могильной<br>
В подвенечном платье, на колени встав:<br>
«Ты прости, мальчишка, я тебя любила,<br>
Но я вышла замуж, без любви устав.<br>
<br>
Не могу любить я мертвого солдата,<br>
Ты лежишь в могиле с красною звездой.<br>
Я еще красивей за два года стала,<br>
Жаль, не получилась у нас жизнь с тобой».<br>
<br>
Так она стояла в подвенечном платье,<br>
И экспресс последний ее домой увез.<br>
И на обелиске по карточке солдатской<br>
Потекли дождинки вместе со слезой.<br>
