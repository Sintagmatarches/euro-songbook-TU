	<h1>БЕЖИТ 
  РЕЧКА ДА ПО ПЕСОЧКУ…</h1>
	
  <br>
  Бежит речка да по песочку, <br>
  Камешки наносит. <br>
  Молодой, молодой солдатик <br>
  Командира просит, <br>
  Молодой, молодой солдатик <br>
  Командира просит: <br>
  <br>
  — Командир мой, ты командир мой, <br>
  Отпусти до дому! <br>
  Там соскучилась, там соскучилась <br>
  Милая зазноба. <br>
  Там соскучилась, там соскучилась <br>
  По мне черноброва. <br>
  <br>
  — Отпустил бы тебя до дому, <br>
  Да долго дома будешь. <br>
  Ты напейся-ка воды холодной – <br>
  Про любовь забудешь. <br>
  Ты напейся-ка воды холодной – <br>
  Про любовь забудешь. <br>
  <br>
  — Пил я воду, да пил холодную, <br>
  Пил — не напивался. <br>
  Любил девицу, да чернобровую, <br>
  Ею любовался. <br>
  Любил девицу, да чернобровую, <br>
  Ею любовался. <br>
  <br>
  Умер, умер тот солдатик <br>
  От тоски по дому. <br>
  И остался лишь конь буланый <br>
  С гривой вороною. <br>
  И остался лишь конь буланый <br>
  С гривой вороною. <br>
  <br>
  Гроб несут, коня ведут, <br>
  Конь головку клонит. <br>
  Идёт девица, чернобровая, <br>
  Горьки слёзы ронит. <br>
  Идёт девица, чернобровая, <br>
  Горьки слёзы ронит. <br>
  <br>
  Течёт речка да по песочку, <br>
  Камешки наносит. <br>
  А другой молодой солдатик <br>
  Ни о чём не просит. <br>
  А другой молодой солдатик <br>
  Ни о чём не просит. <br>
