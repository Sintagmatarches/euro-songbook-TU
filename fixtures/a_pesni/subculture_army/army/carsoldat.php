	<h1>ЦАРСКИЙ 
СОЛДАТ</h1>
	
<br>
Музыка А. Евгеньева<br>
Слова Ивана Белоусова<br>
<br>
В поход, в поход! Бьют барабаны, <br>
Иду за веру и Царя; <br>
Родные ожили поляны: <br>
Над ними торжества заря, - <br>
И тщетно враги злою смертью грозятся, <br>
Коль Богом храним в бою царский солдат!*<br>
<br>
Матушка! Верь: плачешь напрасно! <br>
Полно скорбеть нежной душой: <br>
Без упования - всюду опасно... <br>
Сердцем всегда буду с тобой. <br>
Ты обо мне молись, родная: <br>
Молитва горе усладит. <br>
<br>
<em>Припев. </em><br>
<br>
И бедная матерь молитву творит, <br>
Тихонько рыдая, тихонько рыдая…<br>
"Батюшка! Знай: славою деда <br>
Не перестанет сын твой дышать; <br>
На поле чести будет победа, <br>
Так, как тебя, - меня украшать. <br>
Крест на груди моей не дремлет <br>
Среди громов и жарких сечь". <br>
<br>
<em>Припев. </em><br>
<br>
И тронула старца сыновняя речь: <br>
Он сына объемлет, он сына объемлет... <br>
"Мамаша, прощай! Прощай, невеста, <br>
Радость моя, счастье, любовь! <br>
Горькой тоске в сердце нет места, <br>
Если мы верим, что свидимся вновь! <br>
Моих родимых охраняя, - <br>
Меня, красотка, не забудь..."<br>
<br>
И к другу девица упала на грудь, <br>
Его провожая, его провожая... <br>
В поход, в поход! Бьют барабаны, <br>
Иду за веру и Царя; <br>
Родные ожили поляны: <br>
Над ними торжества заря.<br>
<br>
1864, слова<br>
