	<h1>ЧТО ПОБЕДНЫЕ ГОЛОВУШКИ СОЛДАТСКИЕ...</h1>
	<br>
	Что победные головушки солдатские, 
  <br>
  Они на бой и на приступ — люди первые, <br>
  А к жалованью — люди последние. <br>
  Как со вечера солдатам поход сказан был, <br>
  Со полуночи солдаты ружья чистили, <br>
  Ко белу свету солдаты на приступ пошли. <br>
  Что не грозная туча подымалася, <br>
  Что не черные облака сходилися - <br>
  Что подымался выше облак черный дым, <br>
  Загремела тут стрельба ружейная; <br>
  Что не камушки с крутых плеч покатилися – <br>
  Покатились с плеч головушки солдатские; <br>
  Что не алое сукно в поле заалелось – <br>
  Заалелася тут кровь солдатская; <br>
  Что не белые лебедушки воскликнули – <br>
  Так воскликнут молоды жены солдатские.<br>
  <span class='i'><br>
  Русские народные 
  песни / Сост. и вводн. тексты В.В. Варгановой. М.: Правда, 1988. Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания».</span><br>
