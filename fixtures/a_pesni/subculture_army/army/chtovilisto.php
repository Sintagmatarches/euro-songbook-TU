	<h1>ЧТО ВИЛИСЬ-ТО МОИ РУСЫ КУДРИ, ВИЛИСЬ-ЗАВИВАЛИСЬ...</h1>
	<br>
	Что вил<strong>и</strong>сь-то 
  мои русы кудри, вились-завивались, <br>
  Как заслышали мои русы кудри на себя невзгодье, <br>
  Что большое ли невзгодье, великое безвеременье: <br>
  Что уж быть-то мне, доброму молодцу, во солдатах, <br>
  Что стоять-то мне, доброму молодцу, в карауле, <br>
  Вот стоял я, добрый молодец, в карауле, — <br>
  Пристоялись у доброго молодца мои скоры ноги, <br>
  Как задумал я, добрый молодец, задумал бежать. <br>
  Что бежал я, добрый молодец, не путём-дорогой, <br>
  А бежал я, добрый молодец, темными лесами. <br>
  Во темных лесах, добрый молодец, весь я ободрался, <br>
  Под дождем я, добрый молодец, весь я обмочился, <br>
  Прибежал я, добрый молодец, ко своему подворью. <br>
  Прибежавши, добрый молодец, под окном я постучался; <br>
  «Ты пусти, пусти, сударь батюшка, пусти обогреться! <br>
  Ты пусти, пусти, моя матушка, пусти обсушиться!» <br>
  — «Я б пустила тебя, мое дитятко, боюсь государя! <br>
  Ты поди, поди, мое дитятко, во чистое поле: <br>
  Что буйным ветром тебя, мое дитятко, там обсушит, <br>
  Красным солнышком, мое дитятко, обогреет!» <br>
  Что пошел-то я, добрый молодец, пошел, сам заплакал: <br>
  «Уж возьмись загоритесь вы батюшкины хоромы! <br>
  Уж ты сгинь-пропади матушкино подворье!» <br>
<br>
