	<h1>МОРЯК</h1>
	<br>
Что ты смотришь на море уныло,<br>
  Зачем очи потускли твои?<br>
  И на сердце какая кручина,<br>
  Расскажи мне мечты все свои.<br>
<br>Аль красотка тебя загубила,<br>
  Или бури боишься ночной,<br>
  Нам всегда ведь готова могила<br>
  В глубине, над сердитой волной.<br>
<br>Нашу гибель никто не узнает,<br>
  Не заплачет никто надо мной,<br>
  Только белая чайка услышит<br>
  Крик последний души молодой.<br>
<br>Весть о смерти узнает родная,<br>
  Зарыдает родимая мать,<br>
  Что лишилась сынка дорогого,<br>
  Что ей больше меня не видать.<br>
<br>Вот о чем я задумался, братцы,<br>
  Что охота на свете пожить,<br>
  И на море гляжу я с тоской, ведь<br>
  Еще долго придется служить.<br>
<br>
<span class='i'>В нашу гавань заходили 
