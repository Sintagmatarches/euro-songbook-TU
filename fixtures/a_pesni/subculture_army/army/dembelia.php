	<h1>ДЕМБЕЛЯ </h1>
<br>
Уезжают в родные края<br>
  Дембеля, дембеля, дембеля<br>
  И куда не взгляни в эти майские дни –<br>
  Всюду пьяные ходят они.<br>
  И куда не взгляни в эти майские дни –<br>
  Всюду пьяные ходят они.<br>
<br>
Взгляд последний упал на кафе,<br>
  Не сидеть нам с тобой на губе!<br>
  До свиданья, кусок, мой окончился срок,<br>
  До вокзала последний бросок.<br>
  До свиданья, кусок, мой окончился срок,<br>
  До вокзала последний бросок.<br>
<br>
Вот я вижу свой город родной,<br>
  Он сияет своей красотой.<br>
  Здесь два года назад провожала нас мать,<br>
  Желторотых еще салажат.<br>
  Здесь два года назад провожала нас мать,<br>
  Желторотых своих салажат.<br>
<br>
Открываю знакомую дверь,<br>
  Человек я гражданский теперь.<br>
  Буду пить и гулять, буду женщин ласкать<br>
  И о службе своей вспоминать.<br>
  Буду пить и гулять, буду женщин ласкать<br>
  И о службе своей вспоминать.<br>
<br>
        <span class='i'>С фонограммы Максима Гликина, CD «В нашу гавань заходили корабли» 
        № 4, «Восток», 2001.</span><br>
<br>
<br> 
Популярная песня демобилизованных. Для осенней демобилизации в третьей строке поется &quot;И куда не взгляни в те осенние дни&quot;. В первой строке могут упоминаться не родные края, а те, где проходила служба (например, &quot;Покидают Воркутинские края&quot;). На основе песни Павла Аедоницкого и Якова 
Халецкого <a href="../drugije/vasilki.php">&quot;Васильки&quot;</a>.<br>

