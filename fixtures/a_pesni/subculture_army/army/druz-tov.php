  Друзья-товарищи, простите! <br>
  Лечу в кровавый бой стрелой! <br>
  Напрасно удержать хотите, <br>
  Мне смерть отрада и покой. <br>
  Живите, счастьем наслаждайтесь, <br>
  Коль счастлив в мире человек; <br>
  Но если можете, старайтесь <br>
  Позолотить железный век. <br>
  Поверьте, все на свете ложно. <br>
  Мы видим призраки одни; <br>
  И если быть счастливым можно, <br>
  То верно от одной любви. <br>
  И я любил! Но мне судьбою <br>
  Присуждено лишь слезы лить; <br>
  Война, война, дышу тобою, <br>
  Спешу страданья прекратить! <br>
