	<h1>ЭКСПЕДИЦИЯ 
В АНДИЮ<br>
(«Сухарная экспедиция» 31 мая – 20 июля 1845 г.)</h1>
	Солдатская песня Отряда графа М. Воронцова <br>
<br>
Хорошо житье в Отряде <br>
С Воронцовым-молодцом,<br>
И не трудно нам сражаться <br>
Со чеченцем-подлецом.<br>
<br>
Из-за гор, за гор Кавказских<br>
Во поход Шамиль ступил.<br>
Как увидел отряд русских,<br>
Свою шашку обнажил.<br>
<br>
Он задумал с нами драться,<br>
За Ичкерским лесом ждет.<br>
Поспешай, Шамиль, спасаться –<br>
Воронцов к тебе идет!<br>
<br>
Проходили горы, скалы,<br>
Отыскали путь врагов,<br>
Позабрали все завалы,<br>
Выбив нехристя штыком.<br>
<br>
Спереди наши стрелочки<br>
Пробиралися к врагу,<br>
Казаки поодиночке<br>
&lt;Сломя&gt; смерть несли ему.<br>
<br>
Побежал Шамиль от страху<br>
И от злости задрожал.<br>
Воронцов наш не дал маху<br>
И такую речь держал:<br>
<br>
«Набирайтесь силы, братцы,<br>
Будет с нами, с нами Бог!<br>
Пройдем горы мы Кавказски,<br>
Уничтожим всех врагов!<br>
Пройдем горы мы Кавказски,<br>
Уничтожим всех врагов!»<br>
<br>
<span class='i'>Расшифровка фонограммы Мужского хора Института певческой культуры 
«Валаам», дирижер И. Ушаков, запись 1997 г. CD «Песенная летопись Кавказской войны», 
СПб.: Русская Лира, 2002.</span><br>
 <br>
<br>
<em>31 мая - 20 июля 1845 г.</em> - по старому стилю. Точнее: 15 июня - 31 июля 
(нов. ст.). Воронцов вышел в поход из Гертме 15 июня (нов. ст.), а вернулся в 
Гурзул 1 августа. 3 августа части были разведены по зимним квартирам.<br>
<br>
Цель кампании - утвердиться в Андии - провалилась. Русские обнаружили пепелище 
Анди и тогдашней столицы Шамиля Дарго,  с большими потерями пробились обратно. 
Воронцов потерял 984 убитыми (в том числе трех генералов), 2753 ранеными, 173 
пропавшими без вести, 3 пушки, бывшую при нем большую сумму денег монетой 
и всю поклажу. <br>
<br>
Собственно &quot;Сухарной экспедицией&quot; прозвали попытку русских выручить 
посланные им вслед обозы с продовольствием (это произошло 22-23 июля). В ее ходе 
потери составили 556 убитыми (включая двух генералов - В.М. Викторова и Д.В. Пассека), 858 ранеными и три орудия. <br>

