	<h1>ГДЕ 
МОЙ КОНЬ ВОРОНОЙ </h1>
	<br>
Где мой конь вороной, <br>
Где булатный мой меч? <br>
Я прощуся с женой <br>
И помчуся на сечь. <br>
<br>
За отчизну побьюсь, <br>
Обо мне не тужи: <br>
Я оттуда вернусь <br>
И с крестом на груди. <br>
<br>
Не отдамся живой <br>
На добычу врагам <br>
И своею рукой <br>
Кровью выкуп отдам. <br>
<br>
Нам Царь белый - отец, <br>
А Россия нам - мать! <br>
И в родстве, наконец, <br>
Наша храбрая рать. <br>
<br>
Так прощай же, жена, <br>
О победе молись, <br>
Будь до гроба верна <br>
И не плачь, не крушись!.. <br>
<br>
Если ж в битве паду <br>
Я от вражьей руки, - <br>
Об одном вспомяни: <br>
Я со славой умру.<br>
