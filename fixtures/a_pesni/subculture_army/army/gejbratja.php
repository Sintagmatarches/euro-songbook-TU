  <br>
  Гей, братья, Славяне, гей, воеводы!<br>
  Рабства ярма разобьем мы теперь.<br>
  Уж близится утро желанной свободы,<br>
  Враг уж трепещет как раненый зверь!<br>
  <br>
  Вперед же, Славяне, стройно рядами!<br>
  Бейтесь за волю Славянских племен!<br>
  Россия зовет нас, Великая с нами.<br>
  Гей! собирайтесь у русских знамен.<br>
  <br>
  И лозунгом нашим пусть будет «Вперед»,<br>
  Паролем пусть будет «Свобода и Царь»!<br>
  И скоро сольются в единый народ<br>
  Славяне, как было то встарь!<br>
  <br>
  Все братья единой могучей семьи<br>
  В кровавом бою породнятся опять,<br>
  И гордо от края до края земли<br>
  Славян будет слава венчать!<br>
  <br>
  Так в бой же друзья! Могилы святые<br>
  Пусть отмечают Славянских орлов,<br>
  Пусть села погибли, построим другие<br>
  Только раздавим заклятых врагов!<br>
  <br>
  Гей, братья, Славяне, гей, воеводы!<br>
  Мощен России клич боевой!<br>
  Россия воздвигла знамена свободы,<br>
  Смело ж, Славяне, за старшей Сестрой!<br>
  <br>
  Вперед, вперед, вперед, вперед!<br>
