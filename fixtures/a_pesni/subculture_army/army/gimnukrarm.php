	<h1>ГИМН 
УКРАИНСКОЙ АРМИИ</h1>
<br>
Четвёртые сутки задымлено небо -<br>
То дым от пожаров украинских сёл.<br>
Достаньте горилки, поручик Нетреба,<br>
Корнет Пилипенко, налейте рассол.<br>
<br>
И жовто-блакитный полощется прапор,<br>
На бой вдохновляет нас шкварок дурман.<br>
Поручик Нетреба, гони свиней в табор.<br>
Их пустит на сало корнет Зильберман.<br>
<br>
В ведро со сметаной попала граната,<br>
В засаду попал с колбасою обоз.<br>
Поручик Нетреба, когда же зарплата?<br>
Корнет Пилипенко, хороший вопрос!..<br>
<br>
Зачем жизнь такая без сала и хлеба.<br>
С купонами-гривнами точно помрём.<br>
Пойдёмте сдаваться, поручик Нетреба,<br>
Корнет Пилипенко, в плену хоть пожрём…<br>
