	<h1>ГОП-СО-СМЫКОМ</h1>
  Шоферский вариант <br>
  <br>
  Граждане, послушайте меня,<br>
  Как служил в шоферской роте я.<br>
  Поднимался спозаранку,<br>
  Целый день крутил баранку,<br>
  Вот об этом я пою, друзья.<br>
  <br>
  Был шофером-разгильдяем я.<br>
  Руль крутить — профессия моя.<br>
  А в машине всё грохочет,<br>
  И мотор &lt;...&gt; не хочет -<br>
  Это не касается меня.<br>
  <br>
  Наконец мне, братцы, повезло,<br>
  Когда дорогу снегом замело.<br>
  В новый «ЗИЛ» меня всадили,<br>
  В &lt;кабер&gt; масла мне долили<br>
  И послали в дальнее село.<br>
  <br>
  Заезжаю в крайний ближний дом,<br>
  Старый со старухой жили в нем.<br>
  За тушенку и буханку<br>
  Выставили бражки банку,<br>
  Кислую капусту с огурцом.<br>
  <br>
  Выпил бражки, стал я засыпать,<br>
  Кто-то в окна принялся стучать,<br>
  Командира голос слышу,<br>
  Через дверь я быстро вышел<br>
  И решил скорее удирать.<br>
  <br>
  Чтобы отмахаться от губы,<br>
  А потом прорваться от беды.<br>
  Я к машине подбегаю, <br>
  Зажигание включаю,<br>
  А мотор ни звука не искрит.<br>
  <br>
  И теперь я, братцы, &lt;кофевар&gt;,<br>
  Командир в арест меня упер.<br>
  Дал губы мне десять суток,<br>
  И теперь мне до шуток,<br>
  Я реву, как дизельный мотор.<br>
