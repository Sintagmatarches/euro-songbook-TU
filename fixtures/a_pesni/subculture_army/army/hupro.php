	<h1>РАЗОРЕНИЕ 
АУЛА ХУПРО (август 1849 г.)</h1>

Солдатская песня Отряда генерала Чиляева <br>
<br>
Ой, да не орел, ой, под облаками<br>
В непогодушку летал.<br>
Ой-ли, любо да люл<strong>и</strong>!<br>
В непогодушку летал.<br>
<br>
Ой, да то летал он перед полками,<br>
Наш Чиляев-генерал.<br>
Ой-ли, любо да люли!<br>
Наш Чиляев-генерал.<br>
<br>
Он поздравил нас с походом<br>
И отдал такой приказ.<br>
Ой-ли, любо да люли!<br>
И отдал такой приказ.<br>
<br>
Завтра с солнечным восходом<br>
Подниматься на Кавказ.<br>
Ой-ли, любо да люли!<br>
Подниматься на Кавказ.<br>
<br>
Справа – дьявольские кручи.<br>
Слева – скалы до небес.<br>
Ой-ли, любо да люли!<br>
Слева – скалы до небес.<br>
<br>
Перед носом ходят тучи,<br>
За каблук хватает бес.<br>
Ой-ли, любо да люли!<br>
За каблук хватает бес.<br>
<br>
Верх Кавказа был ночлегом,<br>
С неба сделали шатер.<br>
Ой-ли, любо да люли!<br>
С неба сделали шатер.<br>
<br>
А в котлах – крупа со снегом,<br>
Под котлами – самодёр.<br>
Ой-ли, любо да люли!<br>
Под котлами – самодёр.<br>
<br>
Утром рано встрепенулись<br>
Гренадеры-егеря.<br>
Ой-ли, любо да люли!<br>
Гренадеры-егеря.<br>
<br>
Молодцами потянулись,<br>
Съели по два сухаря.<br>
Ой-ли, любо да люли!<br>
Съели по два сухаря.<br>
<br>
Марш вперед – и затрещали<br>
Басурманские бока.<br>
Ой-ли, любо да люли!<br>
Басурманские бока.<br>
<br>
Все дидойцы тягу дали<br>
От солдатского штыка.<br>
Ой-ли, любо да люли!<br>
От солдатского штыка.<br>
<br>
Мы ударили по-свойски<br>
Кто в лопатку, кто в ребро!<br>
Ой-ли, любо да люли!<br>
Кто в лопатку, кто в ребро!<br>
<br>
И сожгли аул дидойский,<br>
Распроклятое Хупр<strong>о</strong>.<br>
Ой-ли, любо да люли!<br>
Распроклятое Хупро.<br>
<br>
Мы геройски ретираду<br>
Совершили от Хупра.<br>
Ой-ли, любо да люли!<br>
Совершили от Хупра.<br>
<br>
Слава русскому отряду<br>
И Чиляеву ура!<br>
Ой-ли, любо да люли!<br>
И Чиляеву ура!<br>
Ой-ли, любо да люли!<br>
И Чиляеву ура!<br>
