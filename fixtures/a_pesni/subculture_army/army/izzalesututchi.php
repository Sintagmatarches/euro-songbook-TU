	<h1>ИЗ-ЗА ЛЕСУ 
ИДУТ ТУЧИ… </h1>
 <br>Из-за лесу идут тучи, <br>
  Тучи темные, <br>
  Как по мне молодешенькой <br>
  Горе горькое, <br>
  Горе горькое, кручинушка <br>
  Непокрытая, <br>
  Что пошел мой мил сердечный друг <br>
  Во солдатушки, <br>
  Он покинул меня младу <br>
  Одинешеньку. <br>
  Выду ль я млада ранешенько <br>
  В поле чистое, <br>
  Расскажу ль я, расскажу тоску <br>
  Ветру буйному. <br>
  Как несет мне ветер, буйный <br>
  Весть нерадостну: <br>
  Закатилось мое солнышко <br>
  Желанное, <br>
  Уж пал, помер мой сердечный друг.<br> <br>
