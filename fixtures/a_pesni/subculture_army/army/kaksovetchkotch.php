	<h1>КАК СО ВЕЧЕРА КОЧЕТА ПОЮТ...</h1><br>
	Как со вечера кочета поют; 
  <br>
  Со полуночи дружка куют, <br>
  Куют дружка во железушки, <br>
  Везут дружка во солдатушки, <br>
  Во молоденьки некрутики.<br>
<br>Мимо лесу мимо темного, 
  <br>
  Мимо садику зеленого, <br>
  Мимо ельничку-березничку, <br>
  Мимо частого орешничку, <br>
  Пролегала тут дороженька, <br>
  Дороженька не широкая, <br>
  Не широкая, не торная, <br>
  Не торная, не набойная; <br>
  Да никто по ней не хаживал, <br>
  Никто следу не прокладывал. <br>
  Только шли-прошли извозчички, <br>
  Извозчички не тяжелые, <br>
  Не тяжелы — нагруженные, <br>
  С солдатскою амуницией. <br>
  Наперед идут охотнички, <br>
  Позади идут невольнички. <br>
  Охотнички песни взгаркнули, <br>
  Невольнички слезно всплакнули, <br>
  Свою сторону вспомянули: <br>
  «Сторона ль наша, сторонушка, <br>
  Ты знамая и знакомая! <br>
  На сторонушке слободушка, <br>
  На слободушке зазнобушка, <br>
  Зазнобушка — красна девушка!»<br>
