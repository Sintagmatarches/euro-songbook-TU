	<h1>КРАЙ ДОРОЖКИ, КРАЙ ШИРОКИЯ БЫЛО, МОСКОВСКОЙ...</h1>
	<br>
	Край дорожки, край широкия 
  было, московской, <br>
  Тут стояла зелена елинка, <br>
  Не толстая, собою ровная, <br>
  Со вершиночки до самого корня. <br>
  Что на этой было елинке, <br>
  Что на самой было на вершинке, <br>
  Тут сидела дорогая пташка, <br>
  Горегорькая бедная кукушка. <br>
  Не кукует, всё бедна горюет, <br>
  Жалобнешенько, слезно причитает, <br>
  Под елинушку сама взирает: <br>
  Под елинушкой лежит молодчик, <br>
  Не убит он лежит, не застрелен — <br>
  Вострой сабелькой весь он изрублен. <br>
  Что на молодце была рубашка, <br>
  Тонка, беленька была бумажна, <br>
  Во крови-то она была, кумашна, <br>
  Во грязи-то она была, китайна. <br>
  Тут спроговорит душа молодчик: <br>
  «Уж ты мамушка, зелена елина! <br>
  Отпусти ты свое прутьё-витьё! <br>
  Ты прикрой мое тело белое, <br>
  Чтобы дождиком тело не мочило. <br>
  Красным солнышком тело не сушило, <br>
  Чтобы звери тела не терзали, <br>
  Чтобы вороны тела не клевали».<br>
