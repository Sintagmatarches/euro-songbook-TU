        <h1>ПОДВОДНИКАМ С ЛОДКИ «КУРСК»</h1>
        На мотив <a href="raskinulos.php">«Раскинулось море широко»</a> 
        <br>
        <br>
        Случайно иль нет, где ответ на вопрос,<br>
        На связь не выходит подлодка?<br>
        И грянула бедственным выстрелом «SOS»<br>
        Над морем эфирная сводка.<br>
        <br>
        Как будто далекое эхо войны –<br>
        От боли земля застонала,<br>
        А в маленьком домике вдруг со стены<br>
        Икона святая упала.<br>
        <br>
        «О, Господи, что-то там с сыном моим!<br>
        Примета какая плохая…<br>
        Сынок мой родной, возвращайся живым!» -<br>
        Мать тихо шептала, вздыхая.<br>
        <br>
        Но шанс на спасенье судьба не дает –<br>
        Водой затопило отсеки,<br>
        Надежда жива, только сын не придет,<br>
        Он в море остался навеки.<br>
        <br>
        О чем они думали в скорбный свой час –<br>
        Все сто восемнадцать в подлодке?<br>
        Собою спасали от гибели нас<br>
        Герои в матросской пилотке.<br>
        <br>
        <span class='i'>В нашу гавань заходили корабли. Вып. 4. М.: Стрекоза-Пресс, 2001.</span><br>
        <br>
        <br>
        Атомная подводная лодка К-141 «Курск» затонула 12 августа 2000 г. в 
        Баренцевом море в 175 км от Североморска. Все 118 членов экипажа погибли. 
        По неофициальным данным, 24 моряка в девятом отсеке были живы еще более 
        2,5 суток. Президент Путин прервал свой отпуск в Сочи только 17 августа. 
        8 сентября на вопрос Ларри Кинга (канал CNN) о том, что произошло с подлодкой, 
        Путин ответил: &quot;Она утонула&quot;.<br>
        <br>
        Лучшая песня о трагедии &quot;Курска&quot; была написана во время всей 
        этой истории молоденькой бардом из Петербурга Ольгой Макеевой. 
        Она пела ее в том числе на фестивале &quot;Ковчег&quot;, проходившем под 
        открытым небом у Петропавловской крепости. Сценой служила пришвартованная 
        яхта. Это было 2 сентября 2000 г., примерно 17.30. Песня называется 
        <a href="../bard/drugije/podvlodka.php">&quot;Подводная лодка&quot;</a>:<br>
        <br>
        <em>...Подводная лодка лежит на дне.<br>
        В городе М большой человек<br>
        Пустил слезу и сказал: «О нет,<br>
        Мы не забудем о них вовек!<br>
        И встанем дружно за ратью рать,<br>
        Сомкнув ряды супротив беды,<br>
        И будем верить, и будем ждать,<br>
        И под шумок заметать следы...</em><br>
        <br>
        Она спела еще одну хорошую песенку про президента: &quot;...и вроде бы 
        еще не тиран&quot;.<br>

