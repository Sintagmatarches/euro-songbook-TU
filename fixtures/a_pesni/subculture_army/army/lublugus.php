	<h1>ЛЮБЛЮ 
Я ГУСАРИКА </h1>
<br>
Люблю я гусарика, <br>
Люблю молодого. <br>
Дарю я гусарику <br>
Коня вороного. <br>
Коня ль молодого, <br>
Седеличко ново. <br>
Недавно ль гусарик <br>
Из полка приехал; <br>
Недавно он приехал, <br>
Опять уезжает: <br>
Его разлюбезная <br>
Все плачет-рыдает! <br>
Плакала-рыдала, <br>
Ночевать унимала. <br>
"Заночуй-ка, гусарик, <br>
Ты ночку со мною: <br>
Заночуй, молоденький, <br>
Хоть ночку со мною". <br>
"Если заночую, <br>
Взбуди меня раньше: <br>
Ранешенько, рано. <br>
Чтобы не светало; <br>
Чтобы барабанщики <br>
В барабан не били; <br>
Чтобы музыканты <br>
В трубы не играли; <br>
Молоды гусары <br>
Коней не седлали, <br>
Коней не седлали. <br>
С двора не съезжали". <br>
Проснулся гусарик - <br>
На дворе светленько: <br>
"Что ж ты разлюбезная <br>
Надо мной сробила? <br>
Надо мной сробила, <br>
Навеки сгубила!" <br>
