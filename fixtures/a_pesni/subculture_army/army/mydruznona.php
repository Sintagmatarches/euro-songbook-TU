Ритмически не очень аккуратный перевод  неофициального словенского гимна &quot;Naprej zastava slave&quot;, он же &quot;Naprej zastava Slave&quot; (&quot;Вперед, знамя славы&quot; или &quot;Вперед, знамя славян&quot; - смысл названия меняется в зависимости от того, с большой или маленькой буквы начинается последнее слово; в разные эпохи предпочитали разное написание, изначально и в современном варианте пишется с маленькой буквы). <br>
 <br>
  Гимн создан в 1860 г. в ходе борьбы словенской интеллигенции за национальную независимость. Слова Симона Енко, музыка его кузена Даворина Енко, который в 1872 г. напишет также музыку  гимна Сербии &quot;Боже правде&quot;. В 1918-1941 гг. строки песни входили в гимн Югославии, в ходе Второй Мировой войны песня была радиопозывными антифашистского Освободительного фронта Словении, в 1995 г. стала гимном армии Словении. Официальный государственный гимн Словении - &quot;Zdravljica&quot; (с 1989 г.).<br>
  <br>
