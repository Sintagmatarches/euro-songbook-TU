Вы служите, мы замуж пойдем </span><br>
 <br>
День и ночь по эфиру несется,<br>
Отзываясь в солдатских сердцах,<br>
Это песенка ваша поется,<br>
В ней красивые только слова.<br>
<br>
Застучали по рельсам колеса,<br>
Вы нам вслед помахали рукой,<br>
Долго служба солдатская длится,<br>
Мы не скоро вернемся домой.<br>
<br>
Год прошел, мы солдатами стали,<br>
Обещали вы верно нас ждать,<br>
Только верно, кто ждет больше года,<br>
Верно ждет лишь родимая мать.<br>
<br>
Вы о службе лишь только читали,<br>
Но не встали в солдатском строю,<br>
На земле под шинелью не спали,<br>
Не теряли свободу свою.<br>
<br>
Вы лишь песни красиво поете,<br>
И вот полночь, когда уже спят,<br>
Вы к другим на свиданье идете,<br>
А ведь ждать обещали ребят.<br>
<br>
И, конечно, ничуть не смущаясь,<br>
Написали: «Прощай и прости!»<br>
Так зачем же вы верность ребятам<br>
Обещали до смерти нести?<br>
<br>
А вернемся домой после службы,<br>
Ведь не век нам погоны носить,<br>
Только нам объясняться не нужно,<br>
Мы не сможем измены простить.<br>
<br>
Вы при встрече глаза отведете,<br>
Поспешите с дороги сойти,<br>
Вы вернее ребят не найдете,<br>
Мы же лучше сумеем найти!<br>
<br>
