	<h1>&quot;МЫ ВАС ПОДОЖДЕМ&quot;</h1>
    Солдатский ответ<br>
    <br>
День и ночь по эфиру все льется,<br>
Возмущая солдатам сердца,<br>
Эта песенка ваша несется,<br>
Для красивого только словца.<br>
<br>
Год настал, мы солдатами стали,<br>
Обещали со службы нас ждать.<br>
Только редко кто ждет больше года,<br>
Верно ждут лишь отец нас да мать.<br>
<br>
Застучали по рельсам колеса,<br>
Вы вослед помахали рукой.<br>
Долго служба солдатская льется,<br>
И не скоро вернемся домой.<br>
<br>
Вы лишь песни красиво поете,<br>
И в полночь, когда все уже спят,<br>
Вы к другим на свиданье идете,<br>
А ведь ждать обещали ребят…<br>
<br>
Вы вдоль спящих домов проходили,<br>
До утра не сомкнули вы глаз.<br>
Хоть и в армию нас проводили,<br>
Но не стало вам скучно без нас.<br>
<br>
Знаем мы: скоро кончится служба,<br>
Ведь не век нам погоны носить.<br>
Только в нас сомневаться не нужно -<br>
Мы не можем измену простить.<br>
<br>
Вы при встрече глаза отведете,<br>
Поспешите с дороги сойти.<br>
Лучше нас вы ребят не найдете,<br>
Мы же лучше сумеем найти!<br>
<br>
…Мы просили не петь вашу песню,<br>
Не тревожить солдатских сердец,<br>
Ведь без всяких на то обещаний<br>
Верно ждут только мать и отец.<br>
<br>
<span class='i'>Сиреневый туман: Песенник / Сост. А. Денисенко. Новосибирск: Мангазея, 2001. (Хорошее настроение).</span><br>
