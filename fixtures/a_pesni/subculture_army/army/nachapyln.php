	<h1>НАША ПЫЛЬНАЯ ДОРОЖЕНЬКА...</h1>
	<br>
	Наша пыльная дороженька <br>
  Запылена она пылью, <br>
  Эх, запылена она пылью.<br>
<br>По той пыльной по дороженьке <br>
  Много ходу по ней, езду, <br>
  Эх, много ходу по ней, езду,<br>
<br>По той пыльной по дороженьке <br>
  Да шла молодка молодая. <br>
  Эх, шла молодка молодая.<br>
<br>Она, горькая молодка, <br>
  Сама слезно она плакала, <br>
  Эх, сама слезно она плакала.<br>
<br>«Как же мне горькой молодушке, <br>
  Как же мне слезно не плакати? <br>
  Эх, как же мне слезно не плакати?<br>
<br>Моего дружка любезного <br>
  В прием принимают, <br>
  Эх, в прием принимают, <br>
<br>С моего дружка любезного <br>
  Русы кудельки сымают».<br>
