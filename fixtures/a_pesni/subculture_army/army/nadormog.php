	<h1>НА ДОРОГОЙ МОГИЛЕ</h1>
  (Пал ты за край свой родной) <br>
  <br>
  Слова и музыка А. Красносельского<br>
  <br>
  Сквозь слёзы мои, безутешные слёзы<br>
  Гляжу на могилу твою…<br>
  С собой ты унёс мои девичьи грёзы,<br>
  И душу унёс ты мою.<br>
  <br>
  Великий Господь захотел твоей жизни,<br>
  И пал ты за край свой родной.<br>
  Кровь свою отдал святой ты отчизне…<br>
  Так спи же теперь, дорогой.<br>
  <br>
  Невеста твоя безутешным рыданьем<br>
  С тобой на могиле твоей,<br>
  И крест этот, символ святого Страданья,<br>
  Укрыл нас под сенью своей.<br>
  <br>
  Под кровом святым о войне беспощадной,<br>
  О горе земном ты забудь,<br>
  Спи ж, моё счастье, спи, мой ненаглядный,<br>
  Защитой Господь тебе будь!<br>
  <br>
  О, если бы слёзы, сквозь землю пробившись,<br>
  Дошли до тебя, мой родной,<br>
  Ты б весь встрепенулся, от сна пробудившись<br>
  На зов мой душевный, больной.<br>
  <br>
  Но смерть тебя держит с улыбкой злорадной<br>
  В могучих объятьях своих.<br>
  Спи ж, моё счастье, спи, ненаглядный,<br>
  Не слушай рыданий моих.<br>
