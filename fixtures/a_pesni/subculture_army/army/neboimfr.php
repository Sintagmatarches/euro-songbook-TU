	<h1>НЕ 
БОИМСЯ МЫ ФРАНЦУЗОВ </h1>
<br>
Не боимся мы французов,<br>
Шашки остры есть у нас.<br>
Только б дедушка Кутузов<br>
Допустил к ним скоро нас.<br>
Бонапарт хоть и храбрится,<br>
Но у нас попляшет он,<br>
Как удастся нам сразиться;<br>
С нами сам Багратион!<br>
Он Суворова достойный<br>
Ученик храбрейший был,<br>
Подойди к нему поближе -<br>
Так сейчас покажешь тыл!<br>
