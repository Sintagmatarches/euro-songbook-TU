	<h1>НИКТО 
ТАК НЕ СРАЖАЛСЯ, КАК МИЛЫЙ НА ВОЙНЕ</h1>
<br>Не ветер в поле воет,<br>
  Военный гром гремит…<br>
  Никто так не сражался, <br>
  Как милый на войне,<br>
  Он пули не боялся,<br>
  Всё думал обо мне.<br>
  Пришло письмо печально,<br>
  Что милый мой убит,<br>
  Убит, убит, изранен,<br>
  В чистом поле лежит.<br>
  Была бы вольной пташкой,<br>
  Слетала бы туда,<br>
  Собрала б кости вместе,<br>
  Во гробичек склала.<br>
  Во гробичек сложила,<br>
  Земельке предала.<br>
  На том на самом месте<br>
  Построю монастырь,<br>
  Сошью я платье черно,<br>
  Монашенькой пойду.<br>
  Второе – голубое,<br>
  С милым гулять пойду.<br>
  Монашенькой быть можно,<br>
  Но пташкой никогда.<br>
  Все пташки канарейки, <br>
  Все жалобно поют,<br>
  А нам с тобой, мой милый,<br>
  Разлуку придают.<br>
  Разлука ты, разлука,<br>
  Чужая сторона,<br>
  Никто нас не разлучит<br>
  Ни солнце, ни луна.<br>
  Только нас разлучит<br>
  Сырая мать-земля.<br>
  Не ветер в поле воет,<br>
  Военный гром гремит.<br>
  Никто так не сражался,<br>
  Как милый на войне.<br>
<br>
