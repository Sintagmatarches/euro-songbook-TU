	<h1>НУ-КА, 
БРАТЦЫ КАЗАЧЕНЬКИ…</h1>
<br>
Ну-ка, братцы казаченьки, <br>
Станем немцев выручать, <br>
А то немцы трусоваты, <br>
Нам за них, знать, отвечать. <br>
Не боимся этих галлов, — <br>
Шашки вострыя у нас, <br>
Лишь бы батюшка наш Платов <br>
Допустил к ним скоро нас! <br>
Расщелкаем эту сволочь, <br>
Разобьем мы все полки; <br>
Нам не надобна их помощь, <br>
Нам не нужны пруссаки! <br>
Бонапарт хоть и храбрится, <br>
Но напляшется и он, <br>
Как удастся нам сразиться: <br>
С нами князь Багратион! <br>
Он Суворова-Рымникска <br>
Ученик достойный был; <br>
Подойди-ка к нему близко, <br>
Он покажет тебе пыл! <br>
Хоть будь впятеро сильнее, <br>
Он не станет отступать: <br>
Там гораздо он смелее, <br>
Где труднее побеждать. <br>
<br>
