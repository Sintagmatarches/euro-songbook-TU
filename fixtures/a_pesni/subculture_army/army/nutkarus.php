	<h1>НУ-ТКА, РУССКИЕ СОЛДАТЫ </h1>
  <br>
  Ну-тка, русские солдаты, <br>
  Станем немцев выручать! <br>
  Немцы больно трусоваты, <br>
  Нам за них, знать, отвечать. <br>
  <br>
  Не боимся мы французов - <br>
  Штык всегда востер у нас; <br>
  Лишь бы батюшка Кутузов <br>
  Допустил к ним скоро нас! <br>
  <br>
  Расщелкаем их по-свойски. <br>
  Разобьем мы их полки; <br>
  Нам не надо чужи войски, <br>
  Нам не нужны пруссаки! <br>
  <br>
  По полдюжины на брата <br>
  Мы домой приволочем: <br>
  Ведь для русского солдата <br>
  Бить - играть словно мечом. <br>
  <br>
  Бонапарт хоть и храбрится, <br>
  Но напляшется и он, <br>
  Как удастся нам сразиться: <br>
  С нами князь Багратион! <br>
  <br>
  Он Суворова-Рымнинска <br>
  Ученик достойный был; <br>
  Подойди-ка к нему близко - <br>
  Так покажет тотчас пыл! <br>
  <br>
  Вишь, какой неугомонный, <br>
  За Двину к нам перешел <br>
  Враг коварный, лютый, злобный - <br>
  Сам напасть себе нашел. <br>
  <br>
  Хоть будь впятеро сильнее, <br>
  Он не станет отступать; <br>
  Там гораздо он сильнее, <br>
  Где труднее побеждать. <br>
  <br>
  Мы сражаемся за веру, <br>
  Бьем неверных за любовь; <br>
  Не за вздорную химеру <br>
  Проливаем свою кровь! <br>
  <br>
  <span class='i'>Солдатская песня времен антинаполеоновских войн 1805-1807 годов, когда русские 
  солдаты не раз выручали своих союзников, а суворовский генерал князь Багратион 
  одержал ряд блестящих побед. С его именем связан и знаменитый "ледовый поход" 
  через Ботнический залив в 1809 году. Получив смертельное ранение на Бородинском 
  поле, он остался в солдатских песнях героем предыдущих побед. <br>
