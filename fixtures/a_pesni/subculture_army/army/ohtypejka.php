	<h1>ОХ ТЫ ПЕЙ-КА, МОЯ ГОЛОВУШКА, ПРОХЛАДИСЯ...</h1>
<br>
	Ох ты пей-ка, моя головушка, прохладися <br>
  Что за батюшкиной, за матушкиной головою! <br>
  Тошно жить вдове грешной с сиротою, <br>
  А еще тошней жить с богатыми мужиками: <br>
  Богатые мужики — мироеды; <br>
  Собирали они сходы частые, потайные, <br>
  Они думали думу крепкую заедино: <br>
  «Нам кого отдать будет во солдаты? <br>
  Нам богатого отдать — прогневити, <br>
  Одинокого отдать — разорить. <br>
  На ряду-то живет вдова грешная с сиротою, <br>
  У нее есть один сыночек-сиротина, — <br>
  Отдадим-то мы его во солдаты!»<br>
<br>
