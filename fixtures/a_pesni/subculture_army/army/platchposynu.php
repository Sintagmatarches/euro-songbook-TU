	<h1>СИСТЬ БЫЛО МНЕ, ГОРЮШИЦЕ...<br>
	Плач по сыну-рекруту </h1>
	
	Систь было мне, горюшице, 
  <br>
  Бедною да горегорькою, <br>
  Мне под красное окошечко, <br>
  Мне на лавочку дубовую, <br>
  Ко прибоинке кленовою, <br>
  К своему да сыну милому <br>
  К сыну милому-любимому, <br>
  Мне в остатние, в послидние! <br>
  Уж ты, мило мое дитятко, <br>
  Ты бессчастное родилося, <br>
  Бессчастное да бесталанное! <br>
  Куды спешишься да торопишься <br>
  Изо св<strong>о</strong>его ты дому благодатного, <br>
  Изо светлой изо светлицы, <br>
  Из новый да новы горницы? <br>
  Без тебя, да мило дитятко, <br>
  Отемнеет светла светлица, <br>
  Опустает дом-подворьицо! <br>
  Ты подёшь, да мило дитятко, <br>
  Не в любимую да путь-дороженьку,<br>
  Не в любимую — во дальную, <br>
  Во дальную да во печальную, <br>
  Ты во службу-то во царскую, <br>
  Во царскую да государскую, <br>
  Во солдаты новобранные! <br>
  Так ты послушай, мое дитятко, <br>
  Что я тебе буду наказывать, <br>
  Послов<strong>е</strong>чно наговаривать: <br>
  Когда стоскнётся тебе сгорюхнется, <br>
  Как ты подёшь, да мило дитятко, <br>
  На ученье то великое, <br>
  Так примечай-ко, мое дитятко, <br>
  Со которые сторонушки <br>
  Так подует ветер буйной-от: <br>
  Как с полуденные подует-то, <br>
  Так гляди-тко письма-грамоты <br>
  От меня-то, от горюшицы; <br>
  Так напишу я, бедна горькая, <br>
  Напишу письмо-грамоту <br>
  Я не на гербовой бумаге, <br>
  Не пером да не чернилами - <br>
  По гор<strong>ю</strong> да горючим слезам!<br>
  Ты возьми, да мило дитятко, <br>
  Ты возьми да письмо-грамоту, <br>
  Ты возьми да на бел<strong>ы</strong> рук<strong>и</strong>, <br>
  Ты прижми да к ретиву сердцу, <br>
  Что пришло мне письмо-грамотка <br>
  С моей род<strong>и</strong>мы ст<strong>о</strong>роны, <br>
  От родимыя от матушки, <br>
  От кормильца-то от батюшка, <br>
  От соколов от братьев милыих, <br>
  От голубушек мил<strong>ы</strong>х сестер, <br>
  От соседей от соседушек <br>
  И от соседних милых детушек! <br>
  Ты скажи-тко, мое дитятко, <br>
  Когда посулишь в дорог<strong>и</strong> гост<strong>и</strong> — <br>
  О господнеем о праздничке, <br>
  Поутр<strong>у</strong> ли ты ранёшенько, <br>
  Ввечер<strong>у</strong> ли ты позднёшенько, <br>
  Или середи бела дня? <br>
  Так я буду ждать да дожидаться, <br>
  Выходить буду частешенько <br>
  Я на красное крылечушко; <br>
  Я глядеть буду поглядывать, <br>
  Что нейдет ли мое дитятко <br>
  Из дальние да из дороженьки, <br>
  Из городов-то понизовниих, <br>
  Понизовниих украйниих, <br>
  Из солдатов новобраныих? <br>
  Ты простись, да мило дитятко, <br>
  Ты с соседям и с соседушкам, <br>
  И со красныим со девушкам; <br>
  Поблагодари, да мое дитятко: <br>
  На беседе на смиренноей <br>
  Как оне тебя да прилетали, <br>
  Неприятныим словам да называли, <br>
  Оне некрутом считали! <br>
  Как ты пр<strong>и</strong>дешь, да мило дитятко, <br>
  Во свой во дом да благодатной-от, <br>
  Ты придёшь больно не весёл, <br>
  Буйну голову повесил!<br> <br>
  <span class='i'>Причитания / Тексты подготовил К. В. Чистов. Л.: 1960.<br>
  <br>
Русские народные 
  песни / Сост. и вводн. тексты В.В. Варгановой.  М.: Правда, 1988. Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания».</span><br>

