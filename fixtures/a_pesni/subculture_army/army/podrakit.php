	<h1>ПОД 
  РАКИТОЮ ЗЕЛЕНОЙ</h1>
  <br>
  Под ракитою зеленой<br>
  Русский раненый лежал,<br>
  Ко груди, штыком пронзенной,<br>
  Крест свой медный прижимал.<br>
  <br>
  Кровь лилась из свежей раны<br>
  На истоптанный песок;<br>
  Над ним вился черный ворон,<br>
  Чуя лакомый кусок.<br>
  <br>
  «Ты не вейся, черный ворон,<br>
  Над моею головой!<br>
  Ты добычи не дождешься,<br>
  Я солдат еще живой!<br>
  <br>
  Ты слетай в страну родную,<br>
  Отнеси маменьке поклон.<br>
  Передай платок кровавый<br>
  Моей женке молодой.<br>
  <br>
  Ты скажи: она свободна,<br>
  Я женился на другой.<br>
  Я нашел себе невесту<br>
  В чистом поле, под кустом;<br>
  <br>
  Моя сваха – востра сабля,<br>
  И венчал граненый штык;<br>
  Взял невесту тиху, скромну<br>
  И приданно небольшо.<br>
  <br>
  Взял приданно небольшое –<br>
  Много лесу и долин,<br>
  Много сосен, много елок,<br>
  Много, много вересин».<br>
  <br>
  <span class='i'>В нашу гавань заходили корабли. Вып. 2. М.: Стрекоза, 2000.</span><br>
  <br>
  <br>
  Солдатская и казачья песня, сочинена<strong> </strong>Николаем Веревкиным 14 мая 1831 г. после битвы под Остроленкой в ходе подавления польского восстания 1830-31 гг. 
  Строфы, представляющие собой обращение солдата к ворону, бытуют также как самостоятельная 
  песня - <a href="tchernyj.php">«Черный ворон»</a>. <br>
  <br>
  Есть варианты времен Первой мировой (<a href="../ww1/podrakit-ww1.php">"Под ракитою зеленой"</a>) и Гражданской (<a href="../grvojna/kr/podrakit-gr.php">&quot;Под 
  ракитою зеленой...&quot;)</a>.<br>
  <br>
  <br>
  Николай Веревкин, унтер-офицер Невского пехотного полка, с 1-м пехотным корпусом 
  под командованием генерала Ивана Паскевича прошел русско-персидскую войну 1826-1828 гг., 
  русско-турецкую войну 1828-1829 гг.и польскую кампанию 1830-1831 гг., сочинив несколько 
  солдатских песен, которые исполнялись в корпусе и всей армии. Веревкин сочинял 
  свои песни на популярные мелодии.<br>
