  Под ракитою зеленой...</span>
  <br>
   Под ракитою зеленой<br>
  Русский раненый лежал,<br> 
  Ко груди своей пронзенной<br>
  Красный орден прижимал.<br>
  <br>
  Кровь лилась из свежей раны<br> 
  На истоптанный песок,<br>
  А над ним кружился ворон,<br>
  Чуя лакомый кусок.<br>
  <br>
  Вдруг раздался клич военный,<br>
  Снова грянул жаркий бой,<br>
  И, к раките прислоненный,<br>
  Услыхал его герой.<br>
  <br>
  Услыхал и приподнялся,<br>
  Как мертвец среди могил,<br>
  За винтовку быстро взялся,<br>
  Но упал, лишившись сил.<br>
  <br>
  Черный ворон, черный ворон,<br>
  Что ты вьешья надо мной?<br>
  Ты добычи не дождешься -<br>
  Я боец еще живой.<br>
  <br>
  Ты лети-ка, отнеси-ка<br>
  
  Родной маменьке привет.<br>
  А жене моей любимой -<br>
  Кровью облитый кисет.<br>
  <br>
  И скажи, что умер честно<br>
  Я за Родину свою,<br>
  За Советскую державу,<br>
  За советскую звезду.<br>
  <span class='i'><br>
  Второе двустишие куплетов повторяется<br>
  <br>
  Свитова. Запись в Ярославской обл. (1948 г.). Переработка ст-ния поэта 1-й пол. XIX в. Н. Веревкина &quot;Под зеленою ракитой&quot;, написанного 14 мая 1831 г. после битвы под Остреленкой во вреям польского восстания (опубликовано: &quot;Русский инвалид&quot;. 1831, 8 сент., и одновременно отд. луб. изд. в Москве в 1831 г.; см.: Клепиков [Бюллетени Гос. лит. музея, №4. Лубок. Часть I. Русская песня. Составил и комментировал &lt;...&gt; С.А. Клепиков]. С. 116). Песня лишилась шовинистического содержания оригинала и вошла со 2-й пол. XIX в. в репертуар казачьих и солдатских масс (&quot;Отрывки из народного творчества Пермской губ.&quot; Собрал В. Шишонко. Пермь, 1882). Включалось в педставления народной драмы &quot;Шайка&quot; (Ончуков. Сев. народные драмы. СПб., 1911. С. 118) и &quot;Царь Максимилиан&quot; (там же. С. 42, 66. Сб. ОРЯС [Отделения рус. языка и словесности имп. Академии наук]. 1914. Т. 90, №7, С. 111). В годы гражданской и Великой Отечественной войн подвергалось переработке, образец которой представляет комментируемый вар. (ср. концовки в кн.: &quot;Рус. фольклор Великой Отечественной войны&quot;. М.; Л., 1964. С. 365. Вар.: ПиР [Песни и романсы рус. поэтов. Вступ. ст., подг. текста и примеч. В.Е. Гусева. М.; Л., 1965, (Б-ка поэта, БС)]. №676; Гусев [В.Е. Гусев. Рус. нар. песни Южного Урала. Челябинск, 1957]. С. 99; Новикова [Рус. нар. песни. Составила А.М. Новикова. М., 1957]. С. 134; Померанцева-1959. С. 247; Крупянская и Минц [Крупянская В.Ю. и Минц С.И. Материалы по истории песни Великой Отечественной войны. М.: АН СССР, 1953 (&quot;Труды Института этнографии АН СССР: Новая серия&quot;. Т. XIX]. С. 73; Потявин [Нар. поэзия Горьковской обл. Вып. первый. Сост. и ред. В. Потявин. Горьк. гос. ун-т им. Н.И. Лобачевского. Горький, 1960]. С. 264; Свитова, №55). Ст. 1 иногда: &quot;Под ракитою склоненной...&quot;<br>
