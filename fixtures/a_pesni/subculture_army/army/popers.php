  О ПЕРСИДСКОЙ КАМПАНИИ</h1>
	
  Слова Николая Веревкина<br>
 На голос: Уж как пал туман на сине море<br>
  <br>
  Не туман ли из-за моря <br>
  Тучей поднялся? <br>
  Не туман, не дождичек, <br>
  Нет, орел взвился! <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
    Ай, орел взвился! </div>
  Белый как лебедушка, <br>
  Зоркой как сокол, <br>
  Он полки Расейские <br>
  В Персию повел. <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
    В Персию повел!  </div>
  Первый подвернулся нам <br>
  Сам Аббас-Мирза; <br>
  Мы Мирзе с мирзятами <br>
  Плюнули в глаза. <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
    Плюнули в глаза! </div>
  И, в глаза им плюнувши <br>
  Громовым огнем, <br>
  К Эриванской крепости <br>
  Шли минуту с днем. <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
  Шли минуту с днем! </div>
  И, пришедши, начали, <br>
  Видя вражью мочь, <br>
  Шанцы, батареюшки, <br>
  Строить день и ночь. <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
  Строить день и ночь! </div>
  И, построив, в нехристей <br>
  Прежде чем палить, <br>
  Пушечки-мартышечки <br>
  Стали наводить. <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
  Стали наводить! </div>
  И потом, ребятушки, <br>
  Лишь пришла пора, - <br>
  Крикнули по-нашему <br>
  Русское ура! <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
  Русское ура! </div>
  Крикнули, ударили, <br>
  Понеслись на брань, - <br>
  И в секунду с четвертью <br>
  Взяли Эривань! <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
  Взяли Эривань! </div>
