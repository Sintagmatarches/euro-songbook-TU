  Ай, люли люлюшеньки, <br>
   Праздник Покрова! </div>
  И Мирзе Мирзовичу <br>
  Снова дав трезвон, <br>
  Царство басурманское <br>
  Захватил в полон. <br>
  <div class='refren'>
  Ай, люли люлюшеньки, <br>
    Захватил в полон! </div>
    <br>
  <span class='i'>Одна из популярнейших песен русской армии XIX века. А самое удивительное, 
  что, при всей условности песенного языка, события описаны предельно точно. Взятие 
  Эревани, главного оплота Персии на границах с Россией, имело решающее значение 
  в русско-персидской войне 1826-1828 годов. За эту победу генерал от инфантерии 
  Иван Федорович Паскевич был награжден орденом Георгия Победоносца 2-й степени 
  и пожалован графским титулом с прибавлением Паскевич-Эриванский. А противостоял 
  ему наследный сын персидского хана Аббас-Мирза. Упомянутое в песне строительство 
  осадных батарей тоже вполне соответствует исторической действительности. Поразительно 
  точно и указание праздника. Эривань пала 1 октября - в день праздника Покрова 
  Святой Богородицы, что было особым знаком как для русских солдат-христиан, сражавшихся 
  с нехристями, так и для христиан-армян. Такие "детали" мог знать только непосредственный 
  участник событий, каковым и был "корпусной поэт" Николай Веревкин. О нем известно 
  не более, чем о Григории Малышеве или же авторе "Было дело под Полтавой" Иване 
  Молчанове. Единственная публикация его песен состоялась в 1837 году в ХХ томе 
  "Библиотеки для чтения", но и в ней имя автора было указано лишь в качестве 
  примечания: <br>
  <br>
  "Эти песни поются солдатами всего корпуса и сочинены корпусным поэтом, унтер-офицером 
  Невского пехотного полка Николаем Веревкиным". Вместе с 1-м пехотным корпусом 
  под командованием И. Ф. Паскевича он прошел две войны 1826-1829 годов и польскую 
  кампанию 1830-1831 годов, создав несколько солдатских песен, которые пели не 
  только в корпусе, но и во всей армии. Они вошли в песенники без указания имени 
  автора, но именно публикация 1837 года позволяет установить авторство. И в данном 
  случае тексты приводятся не по позднейшим песенникам, а по этой первой и единственной 
  публикации. <br>
  <br>
Антология военной песни / Сост. и автор предисл. В. Калугин. 
  М.: Эксмо, 2006.</span><br>
 <br>
  <br>
  Песня действительно несколько десятилетий была популярна в армии. В частности, вошла в сборник поручика Попова, составленный из песен, исполнявшихся в армии в 1870-80-е гг. (Попов Г.М. Сборник боевых, бытовых и плясовых солдатских песен. Харьков: тип. А. Дарре, 1888). Однако пелась она явно не на мелодию &quot;Уж как пал туман на сине море&quot;, так как последняя написана &quot;кольцовским&quot; пятисложником, а песня Веревкина - трехстопным хореем. Эти размеры плохо совместимы.<br>
