  Припеваючи - люли! <br>
 <div class='refren'>Гей, гей, ого, го, <br>
  Припеваючи - люли! </div>
  И так шли мы сквозь метели, <br>
  Не полгода, полнедели, <br>
 <div class='refren'>Гей, гей, ого, го, <br>
   Не полгода, полнедели! </div>
  Наконец, нам встрелся лях <br>
  И разбит был нами в прах. <br>
 <div class='refren'>Гей, гей, ого, го, <br>
   И разбит был нами в прах! </div>
  Был разбит везде и всюду, <br>
  Где встречали мы Иуду. <br>
<div class='refren'>Гей, гей, ого, го, <br>
    Где встречали мы Иуду! </div>
  Ну, ему ль, затеяв бунт, <br>
  Пошатнуть наш Русский фрунт? <br>
 <div class='refren'>Гей, гей, ого, го, <br>
 Пошатнуть наш Русский фрунт? </div>
  Все смирилось, лишь Варшава, <br>
  Сдуру дуясь, словно пава. <br>
 <div class='refren'>
  Гей, гей, ого, го, <br>
  С дуру дуясь, словно пава! </div>
  Говорила, вся дрожа: <br>
  "Я большая госпожа!" <br>
<div class='refren'>Гей, гей, ого, го, <br>
  "Я большая госпожа!" </div>
  Мы ж ее пощупать кости <br>
  К ней давно сбирались в гости. <br>
<div class='refren'>Гей, гей, ого, го, <br>
  К ней давно сбирались в гости! </div>
  Да не шли!.. концом штыка <br>
  Ей грозя издалека. <br>
<div class='refren'>Гей, гей, ого, го, <br>
  Ей грозя издалека! </div>
   Вдруг, горит светлей алмаза <br>
  И летит - орел с Кавказа. <br>
 <div class='refren'>Гей, гей, ого, го, <br>
   И летит - орел с Кавказа! </div>
  Прилетел, как с неба дождь,<br>
  И над нами стал он вождь. <br>
<div class='refren'>Гей, гей, ого, го, <br>
  И над нами стал он вождь! </div>
  И, вперед рванувшись грудью, <br>
  Повернул Литвой и Жмудью. <br>
<div class='refren'>Гей, гей, ого, го, <br>
  Повернул Литвой и Жмудью! </div>
  И к Варшаве вдруг потом <br>
  Двинул нас как Божий гром. <br>
<div class='refren'>Гей, гей, ого, го, <br>
  Двинул нас как Божий гром! </div>
  И с ней начал перестрелку, <br>
  Да штыковую проделку. <br>
 <div class='refren'>
  Гей, гей, ого, го, <br>
    Да штыковую проделку! </div>
  Прикусил-то лях язык! <br>
  Ведь не свой брат русский штык? <br>
<div class='refren'>Гей, гей, ого, го, <br>
    Ведь не свой брат русский штык? </div>
  Он удал, хоть мал и тонок! <br>
  Взбормотал лях: Падам до ног! <br>
 <div class='refren'>Гей, гей, ого, го, <br>
    Взбормотал лях: Падам до ног! </div>
  И в Варшаву мы вошли <br>
  Припеваючи - люли! <br>
<div class='refren'>
  Гей, гей, ого, го, <br>
   Припеваючи - люли! </div>
  Солнце ж рати христианской, <br>
  Граф Паскевич-Эриванской! <br>
<div class='refren'>Гей, гей, ого, го, <br>
  Граф Паскевич-Эриванской! </div>
  Говорил своим орлам: <br>
  "Исполать, ребята, вам! <br>
 <div class='refren'> Гей, гей, ого, го, <br>
  Исполать, ребята, вам!" </div>
  Нету пятен в Русской славе! <br>
  Мы ура кричим в Варшаве! <br>
 <div class='refren'> Гей, гей, ого, го, <br>
  Мы ура кричим в Варшаве! </div>
  И доволен нами царь. <br>
  Православный государь! <br>
 <div class='refren'>Гей, гей, ого, го, <br>
  Православный государь! </div>
  <span class='i'><br>
  В этой солдатской песне "корпусной поэт" Николай Веревкин вновь соприкоснулся 
  с поэзией Пушкина. И не только Пушкина, но и Жуковского, Дениса Давыдова, которые 
  в польскую кампанию 1830 года отвечали в своих стихах клеветникам Pocсии. Был 
  даже издан специальный сборник "На взятие Варшавы" (СПб., 1831), в который вошли 
  стихи Пушкина и Жуковского с приложением нот. Песня Николая Веревкина вполне 
  бы могла увидеть свет на страницах этого издания, но она опубликована лишь 1837 
  году в "Библиотеке для чтения". Пути Пушкина и унтер-офицера Веревкина не пересеклись 
  ни в Арзруме, ни во время польской кампании. Денис Давыдов, будучи генералом, 
  недаром подчеркивал, что он "не цеховой поэт". Даже ему было не так-то просто 
  стать своим в этом "поэтическом цехе". Что же говорить об "корпусном поэте", 
  унтер-офицере Веревкине. <br>
