 Штурм Варшавы русскими войсками 6-7 октября 1831 г. - эпизод Ноябрьского восстания 1830-31 гг. в Польше (именуемого также русско-польской войной, так как с обеих сторон участвовали регулярные национальные армии). После двухдневного боя на улицах города защитники отступили из Варшавы.<br>
 <br>
Польские песни Ноябрьского восстания см. <a href="../nations/Nations.php#1">"Песни национально-освободительных движений"</a>, среди самых знаменитых - <a href="../polsk/polonezkos.htm">"Полонез Костюшки"</a> и <a href="../polsk/warsz1831.htm">"Варшавянка 1831 года"</a>.<br>
