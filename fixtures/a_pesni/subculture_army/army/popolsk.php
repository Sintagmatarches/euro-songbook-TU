  Православный государь. <br>
 <div class='refren'>Гей, гей, ого, го, <br>
   Православный государь! </div>
  Не пугнул ее словами, <br>
  Нет, повел он лишь бровями. <br>
  <div class='refren'>Гей, гей, ого, го, <br>
    Нет, повел он лишь бровями! </div>
  Да чтоб дело кончить враз, <br>
  Отдал нам такой приказ: <br>
  <div class='refren'>
  Гей, гей, ого, го, <br>
    Отдал нам такой приказ! </div>
  Снарядиться, встрепенуться, <br>
