	<h1>ПОРОДИЛА ДА МЕНЯ МАТУШКА...</h1>
<br>
	Породила да меня матушка, 
  <br>
  Породила да государыня, <br>
  В зеленом-то саду гуляючи, <br>
  Что под грушею под зеленою, <br>
  Что под яблонью под кудрявою, <br>
  Что на травушке на муравушке, <br>
  На цветочках на лазоревых. <br>
  Пеленала да меня матушка <br>
  Во пеленочки во камчатные, <br>
  Во свивальники во шелковые. <br>
  Берегла-то меня матушка <br>
  Что от ветра и от вихоря; <br>
  Что пустила меня матушка <br>
  На чужу дальну сторонушку. <br>
  Сторона ль ты моя, сторонушка, <br>
  Сторона ль ты моя незнакомая! <br>
  Что не сам-то я на тебя зашел, <br>
  Что не добрый меня конь завез — <br>
  Занесла меня кручинушка, <br>
  Что кручинушка великая — <br>
  Служба грозная государева, <br>
  Прытость, бодрость молодецкая <br>
  И хмелинушка кабацкая. <br>
  <span class='i'><br>
Русские народные 
  песни / Сост. и вводн. тексты В.В. Варгановой. М.: Правда, 1988. Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания».</span><br>
<br>
<br>
	Очевидно, песня восходит к рекрутской армии (до введения всеобщей воинской обязанности в 1874 г.) - герой попадает в армию от "хмелинушки кабацкой", т.е. община его сдает в рекруты за плохое поведение.<br>
