        <h1>ПО СИМБИРСКОЙ ПО ДОРОЖКЕ...</h1>
        <br>
       По Симбирской по дорожке<br>
        Колокольчик прозвенел.<br>
        И шли-пошли солдаты,<br>
        Все они молодые.<br>
        За ними идут плачут<br>
        Маменьки родные.<br>
        - Вы не плачьте об нас,<br>
        Маменьки родные.<br>
        Возрыдайте об нас,<br>
        Жены молодые.<br>
        Несходить вам будет<br>
        Чисту травоньку ногами,<br>
        И не засеять вам будет<br>
        Чистое поле семенами.<br>
        Засевалося чистое полюшко,<br>
        Поле все людями.<br>
        Не наполнить вам<br>
        Синее морюшко слезами -<br>
        Наполнялось синее море<br>
        Все ключами.<br>
        С восточной, с дальней стороны,<br>
        Пулей надвигали,<br>
        Молодые офицеры<br>
        Все с квартир съезжали.<br> 
        <span class='i'><br>
        Текст записан т. Дмитриевым К.А. со слов т. Григорьева Л.И., 64 л., неграмотный, сторож Дальстроя, с. Тарбагатай, Тарбагатайского аймака, БМАССР, 1936 г.<br>
        <br>
        Гуревич А.В., Элиасов Л.Е. Старый фольклор Прибайкалья. Т. 1. Улан-Удэ, 1939. С. 306-307. Раздел «Солдатские песни», №181.</span><br>
