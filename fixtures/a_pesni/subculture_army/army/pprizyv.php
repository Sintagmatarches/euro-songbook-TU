	<h1>ПЕСНЯ 
ПРИЗЫВНИКОВ</h1>
<br>
Журавли караваном все на 
  юг улетают,<br>
  Эти крики печали я услышу не раз,<br>
  Слезы мне на плечи упали<br>
  Из-под прекрасных ресниц твоих глаз.<br>
<br>
Скоро наши ребята все на 
  службу уедут,<br>
  Вот и я со дня на день жду приказа властей.<br>
  Очи, что вы смотрите, очи,<br>
  На меня так печально из-под черных бровей?<br>
<br>
Скоро быть мне солдатом. 
  Помни, ты, дорогая,<br>
  Что ни разу за службу не забуду тебя я,<br>
  Помни, я тебя охраняю,<br>
  Чтоб не падали слезы с голубых твоих глаз!<br>
<br>
<span class='i'>В нашу гавань заходили 
  корабли. Вып. 5. М.: Стрекоза, 2001.</span><br>

