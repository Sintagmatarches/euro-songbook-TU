 <br>
  <br>
  Более известна как &quot;Ядерный фугас&quot;. На основе песенки <a href="../baby/golubvagon.php">&quot;Голубой вагон&quot;</a> 
  из мультфильма &quot;Старуха Шапокляк&quot; (режиссер Роман Качанов, 1974, музыка 
  Владимира Шаинского, слова Эдуарда Успенского, там же см. ноты).<br>
  <br>
  Обычно в песне  упоминается &quot;ядерный фугас&quot;, хотя фугасы не бросают в виде бомб, а стреляют ими либо зарывают как мины. Но раз песенка всё равно 
  абсурдная, то это не имеет значения.<br>
  <br>
  <br>
  <br>
  <span class='bold'>ВАРИАНТЫ,  <br>
собранные Кириллом Прохоровым</span><br>
  <br>
  Пародия на песенку "Голубой вагон" из мультфильма про Чебурашку и крокодила 
  Гену. Появилась через два месяца после выхода мультика и обрела громадную популярность 
  среди школьников, студентов и военных. Исполнялась даже на концертах в пионерлагерях. 
  Другие варианты названия: "Ядерный фугас", "Ракетная", "Песня про ракеты", "Юная 
  милитаристская", "Пионерская патриотическая" и т. д.<br>
  <br>
  Иногда песне предпосылается эпиграф: &quot;Я человек старый, мне терять нечего. Я 
  похороню капитализм!&quot; (с) Н. С. Хрущёв.<br>
  <br>
  Народное творчество на эту тему развернулось настолько, что привести здесь все 
  варианты невозможно, да и ненужно. Все они укладываются в определённую схему. 
  В большинстве случаев в песне имеются 1-й куплет ("Медленно ракеты…"), припев 
  и 2-й куплет ("Может мы обидели…"). Количество и содержание последующих куплетов 
  зависит исключительно от памяти и фантазии поющего. Ниже приводятся варианты 
