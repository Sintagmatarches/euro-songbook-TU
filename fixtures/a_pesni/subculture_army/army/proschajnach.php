        <h1>ПРОЩАЙ, НАШ ПИТЕНБУРХ ПРЕКРАСНЫЙ...</h1>
        <br>
        Прощай, наш Питенбурх прекрасный,<br>
        Прощай, столичный городок,<br>
        Прощай, душа красна девчонка,<br>
        Прощай, все милые друзья!<br>
        Считайте дни востальные,<br>
        И часы, и гуляньица ево.<br>
        Служба царская, законная<br>
        Пятнадцать лет царем дана.<br>
        Пришел с ученья весь измокший,<br>
        Свернулся мальчик, лег, уснул.<br>
        Чужая маменька нес скажет:<br>
        - Что ляж сыночек, ляж усни!<br>
        Чужая маменька не скажет:<br>
        - Что сядь, сыночек, сядь поешь,<br>
        А родная маменька сказала: (2 раза):<br>
        Что ляж, сыночек, ляж усни!<br>
      <br>
