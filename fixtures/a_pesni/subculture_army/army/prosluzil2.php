        <h1>ПРОСЛУЖИЛ СОЛДАТ ДВА ГОДА...</h1>
        <br>
        Прослужил солдат два года<br>
        Стал коня к себе ласкать.<br>
        - Конь мой милый,<br>
        Конь ретивый,<br>
        Конь вези меня домой.<br>
        При знакомом переулке<br>
        Конь галопом проскакал.<br>
        Против старово отцовского дома<br>
        Остановился конь, заржал.<br>
        Дом раскидан, дом разбросан,<br>
        Двор зарос весь травой.<br>
        Шел сосед к нему навстречу,<br>
        Все словесно рассказал:<br>
        - Твои сестры вышли замуж,<br>
        Женились братовья,<br>
        Твой отец лежит в больнице,<br>
        Мать давно уж померла.<br>
        Повернул коня направо,<br>
        На кладбище поскакал.<br>
        Привязал коня к забору.<br>
        Сам на кладбище пошел.<br>
        Нашел мамину могилку,<br>
        Сам заплакал, не стерпел.<br>
        - О, ставай, мама родная,<br>
        Сын со службицы пришел,<br>
        Для тебя, мама родная,<br>
        Цвяток розовый рассвел.<br> <span class='i'><br>
        Текст записан т. Дмитриевым К.А. со слов т.т. Шурыгиной А.А., Федоровой А.А. и Сластиной П.А., колхозницы колхоза им. Молотова, с. Б. Куналей, Тарбагатайского аймака, БМАССР, 1937 г.<br>
        <br>
        Гуревич А.В., Элиасов Л.Е. Старый фольклор Прибайкалья. Т. 1. Улан-Удэ, 1939. С. 308. Раздел «Солдатские песни», №184.</span><br>
