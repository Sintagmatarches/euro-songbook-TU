	<h1>ПУГОВИЦЫ 
В РЯД</h1>
<br>
У солдата выходной, пуговицы в ряд.<br>
Ярче солнечного дня, пуговицы в ряд.<br>
Часовые на посту, пуговицы в ряд.<br>
Проводи нас до ворот, пуговицы в ряд,<br>
Пуговицы в ряд.<br>
<br>
<em>Припев:</em><br>
<br>
Идет солдат по городу, пуговицы в ряд.<br>
По незнакомой улице, пуговицы в ряд.<br>
И от улыбок девичьих все пуговицы в ряд,<br>
Все пуговицы в ряд.<br>
Не обижайтесь, девушки,<br>
Ведь для солдата главное,<br>
Все пуговицы в ряд,<br>
Все пуговицы в ряд.<br>
<br>
А солдат попьет кваску, купит эскимо,<br>
Никуда не торопясь, купит эскимо,<br>
И в запасе у него все пуговицы в ряд,<br>
Все пуговицы в ряд.<br>
<br>
