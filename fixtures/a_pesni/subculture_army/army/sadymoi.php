	<h1>САДЫ МОИ, 
САДОЧКИ... </h1><br>Сады мои, садочки, <br>
  Еще зеленые! <br>
  В этих садочках <br>
  Дорожка лежала, <br>
  На этой дорожке кузинка стояла, <br>
  Во этой во кузнице кузнецы ковали, <br>
  Они ковали железы тяжелы! <br>
  На кого же эти железы? <br>
  На кого тяжелы?<br>
  Ни на вора, <br>
  Ни на плута, — <br>
  На того детинку, <br>
  На горькую сиротинку; <br>
  Вечор молодца поймали, <br>
  Ему ноженьки сковали, <br>
  Рученьки связали, <br>
  В санки посадили, <br>
  В город возили, <br>
  В прием водили, <br>
  В ставку становили, <br>
  Головку обрили, <br>
  Медом накормили, <br>
  Вином напоили, <br>
  Спать положили. <br>
  Проснулся молодчик, <br>
  Проснулся удалый: <br>
  «Свет, мои кудри, <br>
  Свет, мои русы! <br>
  Бывало, мои кудри <br>
  По плечам лежали», <br>
  На то молодец уродился, <br>
  В солдаты годился. <br><br>
