Песня о событиях Крымской войны 1853-1856 гг.: в ходе обороны Севастополя Черноморский флот был затоплен, &quot;чтобы загородить кораблям неприятеля вход в Севастопольскую бухту&quot;. Это одно из самых дискуссионных решений в истории русского флота, города оно не спасло. Севастополь после 11-месячной осады был оставлен, хотя другие морские крепости - Николаев и Владивосток, которые находились изначально в худшем положении, чем Севастополь, удержались.<br>

