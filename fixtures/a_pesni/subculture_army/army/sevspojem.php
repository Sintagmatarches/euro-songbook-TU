	<h1>СЕВАСТОПОЛЬЦЫ, 
СПОЕМТЕ </h1>
<br>
Севастопольцы, споемте <br>
Песню новую.<br>
Эх, эй да мы которую певали <br>
На Черном море. <br>
<br>
"Почему ты, запевала-свет, <br>
Свет, состарился?" <br>
- Эх, эй да состарила, чернобровая, <br>
Служба царская. <br>
<br>
Служба царская, боевая, <br>
Да невзгодушка. <br>
Эх, эй да занависла над Россией <br>
Непогодушка. <br>
<br>
Мы не только песни пели, <br>
Но и плакали, <br>
Эх, эй да корабли в бухте топили, <br>
Слезы капали. <br>
<br>
"Евстафия" потопили, <br>
"Трех святителей". <br>
Эх, эй да нам Нахимов-адмирал <br>
Был учителем. <br>
<br>
Воины в синих мундирах <br>
Будут битые. <br>
Эх, эй да наши старые капралы <br>
Страх сердитые. <br>
<br>
На Малаховом кургане <br>
Редут с пушками. <br>
Эх, эй да забросаем синеблузых<br>
Ядрами, шишками. <br>
<br>
Пусть отведают, узнают <br>
Удаль русскую. <br>
Эх, эй да в Черном море утопили <br>
Спесь французскую. <br>
