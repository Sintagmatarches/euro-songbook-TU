	<h1>СЯДЕМ, 
БРАТЦЫ, ВКРУГОВУЮ </h1>
	
<br>
Сядем, братцы, вкруговую, <br>
Грянем песню удалую: <br>
Как живали мы, <br>
Как живали мы! <br>
Вспомним про житье былое, <br>
Бородинское раздолье, <br>
Бородинский бой, <br>
Бородинский бой. <br>
Как наш дедушка Кутузов <br>
Заморил в Москве французов, <br>
В путь их снарядил, <br>
В путь их снарядил. <br>
Бонапарту не до пляски, <br>
Растерял свои подвязки <br>
И кричит «пардон», <br>
И кричит «пардон». <br>
<br>
  <span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. 
   М.: Эксмо, 2006.</span><br>
