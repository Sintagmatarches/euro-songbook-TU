Баллада<br>
<br>
Музыка Петра Булахова<br>
Слова Ивана Лажечникова<br>
<br>
Покоен будь, в неверный час ночной, <br>
На страже ходит часовой: <br>
Покоен будь! <br>
<br>
Полк утомлен горячей битвой; <br>
Давно в шатрах сном крепким спит; <br>
Лишь верный страж его с молитвой <br>
Под Божьей дланью хранит; <br>
<br>
Блеснет ли искра, звук ли канет <br>
В глубокой тишине долин, <br>
И лагерь вмиг в оружье встанет, <br>
Как будто человек один. <br>
<br>
Спи крепко, брат родной! <br>
На страже ходит часовой. <br>
<br>
Раз на рассвете огневая <br>
Пленила брата красота <br>
И говорит ему, лобзая <br>
Его и в очи, и в уста: <br>
<br>
"Старик мой спит, махнув рукой... <br>
А ночь с тобой так хороша! <br>
Побудь еще, побудь со мной,<br>
Солдатик миленькой, душа!" <br>
<br>
Покоен будь, солдатик молодой! <br>
На страже ходит часовой, <br>
Покоен будь. <br>
<br>
&lt;1850-е гг.&gt;, слова<br>
