	<h1>СОЛДАТУШКИ, 
  БРАВО-РЕБЯТУШКИ</h1>
  <br>
  Солдат<strong>у</strong>шки, браво-ребят<strong>у</strong>шки,<br>
  Где же ваши деды?<br>
  «Наши деды – славные победы,<br>
  Вот где наши деды!» <br>
  <br>
  Солдатушки, браво-ребятушки,<br>
  Где же ваши матки?<br>
  «Наши матки – белые палатки,<br>
  Вот где наши матки!»<br>
  <br>
  Солдатушки, браво-ребятушки,<br>
  Где же ваши жены?<br>
  «Наши жены – ружья заряжены,<br>
  Вот где наши жены!»<br>
  <br>
  Солдатушки, браво-ребятушки,<br>
  А есть у вас и тетки?<br>
  «Есть у нас и тетки – две косушки водки,<br>
  Вот где наши тетки!»<br>
  <br>
  Солдатушки, браво-ребятушки,<br>
  Где же ваши сестры?<br>
  «Наши сестры – штыки, сабли остры,<br>
  Вот где наши сестры!»<br>
  <br>
  Солдатушки, браво-ребятушки,<br>
  Где же ваши детки?<br>
  «Наши детки – пули наши метки,<br>
  Вот где наши детки!»<br>
  <br>
  Солдатушки, браво-ребятушки,<br>
  А где же ваша хата?<br>
  Наша хата – лагерь супостата,<br>
  Вот где наша хата!»<br>
  <br>
<span class='i'>«Сборник избранных песен для солдатского хорового пения», 
  Екатеринослав, 1893 г.<br>
  <br>
  Русские песни / Сост. проф. Ив.Н. Розанов. М.: Гослитиздат, 1952.</span><br>
  <br>
  <br>
  Вероятно, восходит к традиционной долитературной солдатской песне (см. <a href="vtchistom.php">&quot;В 
  чистом поле стояло тут дерево&quot;</a>), от которой был взят диалог с солдатами и обработан  по литературным канонам. Получилась четкая рифмованная строевая песня, 
  одна из самых популярных в XIX веке. До сих пор подвергается переделкам - см., 
  например, современную <a href="soldatuchki.php">&quot;Солдатушки&quot;</a>.<br>
