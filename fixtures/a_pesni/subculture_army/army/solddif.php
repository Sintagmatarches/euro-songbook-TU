	<h1>СОЛДАТСКИЙ 
ДИФИРАМБ </h1>
Слова Гавриила Державина<br>
<br>
Спесь мы Франции посбили, <br>
Ей кудерки пообрили, <br>
Убаюкана она! <br>
Уж не будет беспокоить, <br>
Штуки разные нам строить: <br>
<div class='refren'>Дайте чашу нам вина!</div>
Веселися, царь блаженный, <br>
Александр Благословенный! <br>
Русская земля сильна. <br>
О тебе она радела, <br>
Груди, жизни не жалела: <br>
<div class='refren'>Дайте чашу нам вина!</div>
Дайте чашу пьяной браги: <br>
Генералов в честь отваги <br>
Выпьем мы ее до дна; <br>
За казачью хитрость, сбойство, <br>
За солдатское геройство - <br>
<div class='refren'>Дайте чашу нам вина!</div>
Дайте меду нам братину, <br>
Что явили мочь мы львину; <br>
Где пылала зла война, <br>
Сотней тысячи сражали; <br>
Нет храбрей нас - доказали, <br>
<div class='refren'>Дайте чашу нам вина!</div>
Нет храбрей, - что мы с любовью <br>
Своей жертвовали кровью; <br>
Русским честь мила одна: <br>
И корысти забывали, <br>
Мы врагов своих спасали. <br>
<div class='refren'>Дайте чашу нам вина!</div>
Успокоили мы царства; <br>
Бонапарта и коварства <br>
Свергли в бездну адска дна, - <br>
Пусть воюет там с чертями, <br>
Царь-отец! Ты здрав будь с нами. <br>
<div class='refren'>Дайте чашу нам вина!</div>
<br>
10 мая 1814 <br>
<br>
<span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006.</span><br>
