<br>
10 мая 1814 г. - день капитуляции Парижа. Гавриил Державин начал свою карьеру 
в 1862 г. рядовым Преображенского полка; в этом году полк совершил одно из своих 
самых известных &quot;дел&quot; - участвовал в дворцовом перевороте Екатерины 
II. Став впоследствии известным поэтом, Державин за свою долгую жизнь успел воспеть 
в стихах и военные победы эпохи Екатерины, и ее внука Александра I.<br>

