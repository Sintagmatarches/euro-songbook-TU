	<h1>СОЛДАТСКАЯ 
  ПЕСНЯ </h1>
  Слова Григория Малышева<br>
  <br>
  Ну-тка, вспомните, друзья, <br>
  Как за веру и царя <br>
  <div style="text-indent: 32px">Мы ходили,</div> 
  <div style="text-indent: 32px">Покорили</div>
Бусурманскую страну.<br>
  <br>
  Как нас царь Николай <br>
  Сам водил за Дунай, <br>
  <div class='refren'>
  Русь прославил </div>
  <div class='refren'>
  И заставил </div>
  Турок Руси трепетать. <br>
  <br>
  От Дуная враги <br>
  Ускоряли шаги, <br>
  <div class='refren'>
  Все ломали, </div>
  <div class='refren'>
  Истребляли, </div>
  Лишь бы русским не отдать. <br>
  <br>
  Вот и Варна в глазах! <br>
  Веет флаг на стенах, - <br>
  <div class='refren'>
  Разгоралось,</div>
  <div class='refren'>
  Разыгралось </div>
  Сердце русское тогда. <br>
  <br>
  Батареи кругом, <br>
  Рвы и мины с огнем, <br>
  <div class='refren'>
  Стены рвали,</div>
  <div class='refren'>
  Исчезали - </div>
  В дымном облаке враги. <br>
  <br>
  Море Черное шумит, <br>
  С кораблей огонь летит, <br>
  <div class='refren'>
  Крепость рушит,</div>
  <div class='refren'>
  Турок душит,</div>
  Варна пламенем горит. <br>
  <br>
  Царь пред нами летал <br>
  И полки ободрял - <br>
  <div class='refren'>
  Драться смело,</div>
  <div class='refren'>
  Помнить дело,</div>
  Не ударить в грязь лицом. <br>
  <br>
  Храбрый Бистром с нами был, <br>
  Он к победам нас водил: <br>
  <div class='refren'>
  С ним все брали,</div>
  <div class='refren'>
  Не считали</div>
  Мы врагов, - вперед! ура! <br>
  <br>
  Там случалося не раз <br>
  Смерть встречать нам глаз на глаз; <br>
  <div class='refren'>
  Как бывало,</div>
  <div class='refren'>
  Напропало -</div>
  Грянем дружно на штыки. <br>
  <br>
  Как сойдемся грудь на грудь, <br>
  Некрещеные бегут; <br>
  <div class='refren'>
  Где поганым</div>
  <div class='refren'>
  Бусурманам</div>
  Против русских устоять? <br>
  <br>
  Царь сам раненых встречал <br>
  И крестами украшал; <br>
  <div class='refren'>
  Верно павших,</div>
  <div class='refren'>
  Пострадавших</div>
  Бог венцами награждал... <br>
  <br>
  Лишь заря занялась, <br>
  Крепость русским сдалась; <br>
  <div class='refren'>
  Враг робеет,</div>
  <div class='refren'>
  В Варне веет</div>
  Знамя Белого Царя! <br>
  <br>
  Еще шаг - и нам Царь-град, <br>
  Дай ключи. Хоть рад - не рад; <br>
  <div class='refren'>
  Но покорных,</div>
  <div class='refren'>
  Не задорных</div>
  Меч не рубит, не сечет. <br>
  <br>
  Образумился Султан, <br>
  Просит мира Оттоман. <br>
  <div class='refren'>
  Служба службой,</div>
  <div class='refren'>
  Дружба дружбой,</div>
  И от мира - мы не прочь. <br>
  <br>
  Царь спасибо нам сказал, <br>
  Молодцами называл; <br>
  <div class='refren'>
  По-солдатски,</div>
  <div class='refren'>
  Залихватски -</div>
  Отвечали мы: "Ура!!!" <br>
  <br>
  Чтоб любить Россию-мать, <br>
  Защищая умирать <br>
  <div class='refren'>
  Нас учили,</div>
  <div class='refren'>
  Нам твердили,</div>
  Заповедали отцы. <br>
  <br>
  Честь и слава нам сродни, <br>
  Нам в крови переданы; <br>
  <div class='refren'>
  Наши деды</div>
  <div class='refren'>
  Нам к победам </div>
  Проложили славный путь. <br>
  <br>
  Ведь полтавский-то гром, <br>
  Напущенный Петром, <br>
  <div class='refren'>
  Чуть живого</div>
  <div class='refren'>
  И хромого </div>
  Карла в Турцию загнал. <br>
  <br>
  Вся Европа шла на нас, <br>
  А Господь один за нас! <br>
  <div class='refren'>
  Угостили,</div>
  <div class='refren'>
  Уложили</div>
  Дерзких незваных гостей. <br>
  <br>
  Эривань и Ахалцик <br>
  Будут помнить русский штык, <br>
  <div class='refren'>
  Не хвалитесь,</div>
  <div class='refren'>
  Не грозитесь,</div>
  Лиходеи... с нами Бог! <br>
  <br>
  Пал сармат навсегда <br>
  И не встанет никогда; <br>
  <div class='refren'>
  Эриванский -</div>
  <div class='refren'>
  Князь Варшавский</div>
  Разом буйный дух смирил. <br>
  <br>
  Слава русским царям! <br>
  Слава русским вождям! <br>
  <div class='refren'>
  Слава храбрым</div>
  <div class='refren'>
  Воям ратным,</div>
  Слава всей Руси, ура!!!<br>
