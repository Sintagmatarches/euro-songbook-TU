  <span class='i'>Песня входила в репертуар Придворной певческой капеллы и была коронной в 
  выступлениях Григория Малышева. Особую роль в его судьбе сыграл генерал Карл 
  Бистром, которого солдаты перекрестили на свой лад в Быстрова. В Бородинском 
  сражении он первым выступил в бой, дошел до Парижа. В 1828 году солдаты под 
  его командованием взяли на штык Варну. Именно Бистром в 1835 году не только 
  представил трем монархам своего музыканта Григория Малышева, но и сочинил музыку 
  на его стихи. Он умер от ран в 1838 году. Малышев и другие солдаты всю жизнь 
  сохраняли память о своем генерале. <br>
