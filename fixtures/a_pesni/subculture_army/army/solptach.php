	<h1>СОЛОВЕЙ, СОЛОВЕЙ, 
ПТАШЕЧКА </h1>
<br>Слушай, братцы, мой приказ:<br>
  “Поведу я в баню вас.<br>
  Как скомандую: ать, два!<br>
  Запевайте соловья”.<br>
<br>Эй, соловей, соловей, пташечка,<br>
  Канареечка жалобно поет.<br>
  Эй, раз! Эй, два! Горе – не беда,<br>
  Канареечка жалобно поет.<br>
<br>Парикмахером я был,<br>
  Усы, бороды я брил;<br>
  А забрили и меня –<br>
  Распеваю соловья.<br>
<br>Эй, соловей...<br>
<br>Раньше нежным баритоном<br>
  В оперетке шпарил я,<br>
  А теперь солдатским тоном<br>
  Распеваю соловья.<br>
<br>Эй, соловей...<br>
