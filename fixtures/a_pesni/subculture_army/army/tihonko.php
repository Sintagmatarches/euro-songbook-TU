	<h1>ТИХОНЬКО 
  ПОЕЗД ГРОМЫХАЕТ</h1>
	
  <br>
  Тихонько поезд громыхает<br>
  Вечерней позднею зарей. <br>
  В вагонах песни раздаются,<br>
  Солдаты едут все домой.<br>
  <br>
  Один солдат нахмурил брови,<br>
  Поник он буйной головой;<br>
  И он запел про кари очи,<br>
  Про очи девушки одной:<br>
  <br>
  «Ах, очи, очи голубые,<br>
  Вы сокрушили молодца,<br>
  Зачем, зачем вы, люди злые,<br>
  Вы их разрушили сердца?<br>
  <br>
  Она меня крепко любила,<br>
  И я ухаживал за ней…<br>
  Теперь она меня забыла,<br>
  Не быть уж ей теперь моей.<br>
  <br>
  Когда в солдаты провожала,<br>
  Она слезами залилась;<br>
  Теперь письмо мене прислала –<br>
  За брата замуж собралась.<br>
  <br>
  Приду домой я на свиданье,<br>
  Заколет сердце, как иглой.<br>
  А брат подумает с насмешкой,<br>
  Что опоздал соперник мой.<br>
  <br>
  Пройдут года, родятся дети<br>
  И будут дядей меня звать…<br>
  Они не знают, что их дядя<br>
  Любил когда-то вашу мать».<br>
  <br>
  <span class='i'>В нашу гавань заходили корабли. Вып. 2. М.: Стрекоза, 2000.</span><br>
