	<h1>ТЫ 
СКАЖИ, МОЯ МАРУСЕНЬКА </h1>
<em>Солдатская шуточная песня-припевок </em><br>
<br>
"Ты скажи, моя Марусенька, <br>
Не утаи, моя душенька, <br>
Кто у тебя были гости без меня? <br>
Кто у тебя были гости без меня?" <br>
<br>
"Бригадир был Преображенский, <br>
А майор лейб-гренадерский, <br>
Поручик-голубчик <br>
Был Московского полка". <br>
<br>
"Ты скажи, моя Марусенька, <br>
Не утаи, моя душенька, <br>
А что без меня <br>
Ели гости у тебя?" <br>
<br>
"Бригадир ел курочку, <br>
А майор ел уточку; <br>
Поручик-голубчик <br>
Ел рябчика со мной". <br>
<br>
"Ты скажи, моя Марусенька, <br>
Не утаи, моя душенька, <br>
А что без меня <br>
Пили гости у тебя?" <br>
<br>
"Бригадир пил водочку, <br>
А майор - вишневочку; <br>
Поручик-голубчик <br>
Пил малиновку со мной". <br>
<br>
"Ты скажи, моя Марусенька, <br>
Не утаи, моя душенька, <br>
А где без меня <br>
Спали гости у тебя?" <br>
<br>
"Бригадир спал во горнице, <br>
А майор спал в светлице; <br>
Поручик-голубчик <br>
Спал во спаленке со мной". <br>
<br>
<span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006.</span><br>
<br>
<br>
Песня была популярна в армии в 1870-80-е гг. Термином &quot;припевок&quot; (сегодня для него используется женский род - &quot;припевка&quot;) солдаты называли песни, которые на марше исполнялись в плясовой манере - армейские песенники приплясывали, играли на музыкальных инструментах; кроме того, эти песни, в отличие от &quot;обычных&quot; маршевых песен, часто исполнялись полностью хором, а не в режиме чередования запевалы и хора.<br>

