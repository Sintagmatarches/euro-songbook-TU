        <h1>ТЫ ВЗОЙДИ-КА, КРАСНО СОЛНЫШКО...</h1>
        <br>
        Ты взойди-ка, красно солнышко,<br>
        Над горой взойди высокой,<br>
        Над зеленью.<br>
        Обогрей-ка, красно солнышко,<br>
        Ретивое мое сердце,<br>
        Ретивое сердце молодецкое,<br>
        Молодецкое, солдацкое,<br>
        Солдат бедненьких,<br>
        Бесквартирненьких.<br>
        Бежит речка быстрая,<br>
        Каменистая,<br>
        Как во той речке<br>
        Плывут два суденышка,<br>
        Третья лодочка.<br>
        Хорошо лодка разукрашена,<br>
        Нос и корма позолочены,<br>
        Черным бархатом околочена.<br> 
       <br>
