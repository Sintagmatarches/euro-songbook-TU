	<h1>У МУЖИКА БЫЛО БОГАТОГО...</h1> 
	<br>
	У мужика было богатого, 
  <br>
  Да и три сына были хорошие. <br>
  Вот и вышло на них несчастьице – <br>
  Да невольщина-некрутчина! <br>
  Да и старшего сына жаль отдать, <br>
  А середнего не хочется; <br>
  Что идти ль не идти сыну меньшему. <br>
  Как и меньший сын расплачется: <br>
  «Ах, и я ли вам не родный сын?» <br>
  Как возговорит-то и батюшка: <br>
  «Ах, и дети мои вы родные, <br>
  Ах, идите-тка во зеленый сад <br>
  Да вы срежьте по жеребью, <br>
  Ну вы бросьте-тка жеребий!» <br>
  Как досталось сыну старшему. <br>
  Старший сын пригорюнился, <br>
  Молода жена его расплакалася, <br>
  Малы дети разрыдалися. <br>
  Как возговорит да и меньший сын: <br>
  «Да не плачьте ж вы, да и родный: <br>
  Я иду за вас охотою». <br>
<br>
<span class='i'>Русские народные 
  песни / Сост. и вводн. тексты В.В. Варгановой. М.: Правда, 1988.  Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания»</span>.<br>
