<span class='bold'>ВАРИАНТ<br>
 <br>
У отца было, у матери три 
	  сына любимыих...</span><br><br>У отца было, у матери три 
	  сына любимыих. <br>
	  Отец с матерью всю ночь не спят, <br>
	  Всю ночь не спят, за столом сидят, <br>
	  За столом сидят, думу думают: <br>
	  «Нам которого сына в солдаты отдать? <br>
	  Большого-то отдать — детей много; <br>
	  Среднего отдать — жена хороша, <br>
	  Жена хороша, больно услужлива, <br>
	  Уж отдать ли, нет ли сына малого, <br>
	  Сына малого, неженатого». <br>
	  А меньшой-ет сын расплакался, <br>
	  Отцу с матерью разжаловался: <br>
	  «Государь ты мой родной батюшка, <br>
	  Государыня моя матушка! <br>
	  Или я вам не тот же сын? <br>
	  Или я вам не кормилец был?» <br>
	  — «Уж вы дети мои милые! <br>
	  Уж вы все-то мне малехоньки! <br>
	  Малехоньки и больнехоньки! <br>
	  Вы возьмите-ка по ножичку, <br>
	  Уж вы срежьте-ка по прутику; <br>
	  Сами сделайте по жеребью <br>
	  И метните-ка по жеребью, <br>
	  Что которому достанется!» <br>
	  Доставалося большому брату. <br>
	  Большой брат расплакался, <br>
	  Перед батюшкой с матушкой стоючи: <br>
	  «Государь ты мой родной батюшка, <br>
	  Государыня моя матушка! <br>
	  Не покиньте мою молоду жену, <br>
	  Молодую жену, малых детушек!» <br><br>
	  <span class='i'>Русские народные 
  песни / Сост. и вводн. тексты В.В. Варгановой. М.: Правда, 1988.  Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания»</span>.<br>

