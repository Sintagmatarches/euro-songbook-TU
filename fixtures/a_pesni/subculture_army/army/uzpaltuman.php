  <br>
  Ах, пал туман на сине море, <br>
  Вселилася кручина в ретиво сердце, <br>
  Не схаживать туману со синя моря, <br>
  Злодейке кручине с ретива сердца! <br>
  Что далече, далече во чистом поле, <br>
  Стояла тут дубровушка зеленая, <br>
  Среди ее стоял золотой курган, <br>
  На кургане раскладен был огничек, <br>
  Возле огничка постлан войлочек, <br>
  На войлочке лежит ли добрый молодец, <br>
  Припекает свои ранушки боевые, <br>
  Боевые ранушки кровавые. <br>
  Что издалека, далека, из чиста поля <br>
  Приходят к нему братцы-товарищи, <br>
  Зовут ли доброго молодца на святую Русь. <br>
  Ответ держит добрый молодец: <br>
  «Подите, братцы, на святую Русь, <br>
  Приходит мне смертонька скорая, <br>
  Отцу-матери скажите челобитьице, <br>
  Роду-племени скажите по поклону всем, <br>
  Молодой жене скажите свою волюшку <br>
  На все ли на четыре на сторонушки, <br>
  Малым детушкам скажите благословеньице. <br>
  Ах, не жаль-то мне роду-племени, <br>
  Не жаль-то мне молодой жены, <br>
  Мне жаль-то малых детушек, <br>
  Осталися детушки малешеньки, <br>
  Малешеньки детушки, глупешеньки; <br>
  Натерпятся голоду и холоду». <br>
