	<h1>УЖ 
  КАК ПАЛ ТУМАН НА СИНЁ МОРЕ… </h1>
  <br>
  Уж как пал туман на синё море, <br>
  А злодейка кручина в ретиво сердце, <br>
  Как не схаживать туману со синя моря,<br>
  А злодейке кручине с ретива сердца! <br>
  Как далече, далече во чистом поле <br>
  Разгорался огонечек малешенек; <br>
  Возле огничка постлан ковричек, <br>
  А на ковричке лежит добрый молодец, <br>
  Во правой руке держит тугой лук, <br>
  Во левой руке — калену стрелу, <br>
  Во скорых ногах стоит добрый конь, <br>
  Он и бьет копытом о сыру землю, <br>
  И он знать дает добру молодцу: <br>
  «Ты вставай, вставай, добрый молодец! <br>
  Ты садись на меня, на добра коня, <br>
  Я свезу тебя к отцу, к матери, <br>
  К молодой жене, к малым детушкам!» <br>
  Как возговорит добрый молодец: <br>
  «Ты мой добрый конь, слуга верный мой, <br>
  Поезжай один во святую Русь, <br>
  Поклонись от меня отцу-матери, <br>
  Челобитье моей молодой жене, <br>
  Благословенье скажи малым детушкам, <br>
  Ты скажи, объяви молодой вдове, <br>
  Что женился я на иной жене, <br>
  Что я взял за себя поле чистое, <br>
  А в приданы взял зелены луга; <br>
  У нас добрый сват был булатный меч, <br>
  А сосватала калена стрела, <br>
  На постель клала свинцова пуля...» <br>
