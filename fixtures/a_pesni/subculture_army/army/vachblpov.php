	<h1>ВАШЕ БЛАГОРОДИЕ, ГОСПОЖА ПОВЕСТКА</h1>
<br>
Ваше благородие, госпожа повестка!<br>
  На два года увезли из родного места.<br>
  За двумя границами и от друзей вдали<br>
  Не везет мне в службе, повезет в любви.<br>
<br>
Ваше благородие, госпожа «учебка»,<br>
  Засорила мне мозги на два года крепко,<br>
  Каждый день спецуха, физо и тренажи,<br>
  Не везет мне в службе, повезет в любви.<br>
<br>
Ваше благородие, наш бетонный плац,<br>
  Сапоги уже дымят и круги из глаз.<br>
  Ногу задираю выше головы,<br>
  Не везет мне в службе, повезет в любви.<br>
<br>
Ваше благородие, господин «кусок»,<br>
  Покатался ты на мне, как уж только смог.<br>
  Драил я казарму с ночи до зари,<br>
  Не везет мне в службе, повезет в любви.<br>
<br>
Ваше благородие, госпожа Столовая,<br>
  Кормишь ты нас кашею, в основном перловою,<br>
  Двадцать граммов масла, мяса не ищи,<br>
  Не везет мне в службе, повезет в любви.<br>
<br>
Ваше благородие, господин Приказ,<br>
  Я тебя два года ждал и дождусь сейчас.<br>
  Ох, как долго тянутся последние деньки,<br>
  Не везет мне в службе, повезет в любви.<br>
<br>
Ваше благородие, их Превосходительство,<br>
  Вот и отдан был приказ партии, правительства.<br>
  Навернулись слезы на глаза мои,<br>
  Не везет мне в службе, повезет в любви.<br>
<br>
<span class='i'>В нашу гавань заходили корабли. Вып. 4. М.: Стрекоза, 2001.</span><br>
