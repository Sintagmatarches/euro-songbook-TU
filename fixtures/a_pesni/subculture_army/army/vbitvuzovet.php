<h1>В БИТВУ ЗОВЁТ ТЕБЯ РУСЬ И НАРОД!<br>
  (Я тебя никому не отдам) </h1>

  Слова и музыка А. Красносельского <br>
  <br>
  Долг пред Отчизной, святой, неизбежный<br>
  Отнял тебя у меня, дорогой.<br>
  Где ж ты желанный мой, где ты мой нежный,<br>
  В чьи же края занесён ты судьбой!<br>
  <br>
  Может быть, высший приказ исполняя,<br>
  Скачешь над пулями ты на коне,<br>
  Может быть, кровью от ран истекая,<br>
  Мыслью последней летишь ко мне…<br>
  <br>
  Где бы ты ни был запомни, любимый,<br>
  Думы мои безотлучно с тобой,<br>
  Жарко молюсь я Творцу, мой родимый,<br>
  Жадно прошу ниспослать мне покой.<br>
  <br>
  С твёрдою верой читая молитву,<br>
  С ясной надеждой смотрю я вперёд, -<br>
  Смело ж иди, мой желанный, ты в битву,<br>
  В битву завёт тебя Русь и народ.<br>
  <br>
  Если героем ко мне ты вернёшся,<br>
  О, сколько счастья у нас впереди!<br>
  Крепко, родной мой, ко мне ты прижмёшся,<br>
  К верной, горячей прильнёшь ты груди.<br>
  <br>
  Если ж захочет Господь твоей жизни, -<br>
  Я за тобой улечу к небесам,<br>
  Только тебя, в этой новой отчизне,<br>
  Я никому не отдам!..<br>
