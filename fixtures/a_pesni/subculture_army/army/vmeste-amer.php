  Если только уцелеем,<br>
  Если только не получим мы под… шею.<br>
  Кто по шее? Как по шее?!<br>
  Прокукуй же нам, кукушечка иракская,<br>
  Где и как придёт конец далеким странствиям.<br>
  Мы, похоже, с партизанами не справимся.<br>
  В оцинкованных гробах домой отправимся…<br>
