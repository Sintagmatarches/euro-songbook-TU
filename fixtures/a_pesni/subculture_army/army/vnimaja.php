<h1>ВНИМАЯ 
УЖАСАМ ВОЙНЫ…</h1>
Слова Николая Некрасова<br>
<br>
Внимая ужасам войны, <br>
При каждой новой жертве боя<br>
Мне жаль не друга, не жены, <br>
Мне жаль не самого героя... <br>
<br>
Увы! утешится жена, <br>
И друга лучший друг забудет; <br>
Но где-то есть одна душа - <br>
Она до гроба помнить будет! <br>
<br>
Средь лицемерных наших дел <br>
И всякой пошлости и прозы <br>
Один я в мире подсмотрел <br>
Святые, искренние слезы - <br>
<br>
То слезы бедных матерей! <br>
Им не забыть своих детей, <br>
Погибших на кровавой ниве; <br>
Как не поднять плакучей иве <br>
Своих поникнувших ветвей. <br>
<br>
&lt;1856&gt;<br>
