 <h1>ВОЕННАЯ 
  ПЕСНЯ, <br>
  НАПИСАННАЯ ВО ВРЕМЯ ПРИБЛИЖЕНИЯ НЕПРИЯТЕЛЯ <br>
  К СМОЛЕНСКОЙ ГУБЕРНИИ </h1>
  Слова Федора Глинки<br>
  <br>
  Раздался звук трубы военной, <br>
  Гремит сквозь бури бранный гром: <br>
  Народ, развратом воспоенный, <br>
  Грозит нам рабством и ярмом! <br>
  Текут толпы; корыстью гладны, <br>
  Ревут, как звери плотоядны, <br>
  Алкая пить в России кровь. <br>
  Идет. Сердца их жесткий камень, <br>
  В руках вращают меч и пламень <br>
  На гибель весей и градов!<br>
  <br>
  В крови омочены знамена <br>
  Багреют в трепетных полях, <br>
  Враги нам вьют вериги плена, <br>
  Насилье грозно в их полках. <br>
  Идут - влекомы жаждой дани. <br>
  О страх! срывают дерзки длани <br>
  Со храмов Божьих лепоту! <br>
  Идут, и след их - пепл и степи! <br>
  На старцев возлагают цепи, <br>
  Влекут на муки красоту! <br>
  <br>
  Теперь ли нам дремать в покое, <br>
  России верные сыны?! <br>
  Пойдем, сомкнемся в ратном строе, <br>
  Пойдем - и в ужасах войны <br>
  Друзьям, отечеству, народу <br>
  Отыщем славу и свободу <br>
  Иль все падем в родных полях! <br>
  Что лучше: жизнь - где узы плена, <br>
  Иль смерть - где русские знамена? <br>
  В героях быть или в рабах? <br>
  <br>
  Исчезли мира дни счастливы, <br>
  Пылает зарево войны. <br>
  Простите веси, паствы, нивы!<br>
  К оружью, дети тишины! <br>
  Теперь, сейчас же, мы, о други! <br>
  Скуем в мечи серпы и плуги: <br>
  На бой теперь - иль никогда! <br>
  Замедлим час - и будет поздно! <br>
  Уж близко, близко время грозно: <br>
  Для всех равно близка беда! <br>
  <br>
  И всех, мне мнится, клятву внемлю: <br>
  Забав и радостей не знать, <br>
  Доколе враг святую землю <br>
  Престанет кровью обагрять! <br>
  Там друг зовет на битву друга, <br>
  Жена, рыдая, шлет супруга, <br>
  И матерь в бой своих сынов! <br>
  Жених не мыслит о невесте, <br>
  И громче труб на поле чести <br>
  Зовет к отечеству любовь. <br>
  <br>
  Июль 1812 <br>
  <br>
 <span class='i'>Как и легендарный поэт-гусар Денис Давыдов, преображенцы Сергей Марин, Павел 
  Катенин, ополченец Константин Батюшков, он был участником всех антинаполеоновских 
  войн 1805-1807 годов, в 1812 году на Бородинском поле сражался вместе с ополченцами 
  Петром Вяземским и Василием Жуковским. Его "Письма русского офицера" - один 
  из самых выдающихся памятников русской военной прозы, а <a href="soldpesn.php">"Солдатская 
  песнь"</a>, "Военная песнь", <a href="avangardp.php">"Авангардная песнь"</a>, 
  созданные в июле 1812 года в действующей армии, предшествовали <a href="pevecvostane.php">"Певцу 
  во стане русских воинов"</a> Жуковского. Не говоря уже о том, что его <a href="../popular20/neslychnochumu.htm">"Песнь 
  узника"</a> ("Не слышно шуму городского") лежит у истоков "тюремных" песен, 
  <a href="../popular20/trojka.htm">"Мчится тройка удалая"</a> положила начало всем остальным 
  песенным "Тройкам", стихотворение "Город чудный, город древний" стало одним 
  из первых и самых известных песенных гимнов Москвы. Таковым был Федор Глинка, 
  не избежавший в своей долгой, почти столетней жизни ни сумы, ни тюрьмы, но навсегда 
  сохранивший верность своему призыву: "Вера нам и верность свята". </em><br>
