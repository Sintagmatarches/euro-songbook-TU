	<h1>В ЧИСТОМ ПОЛЕ СТОЯО ТУТ ДЕРЕВО...</h1>
<br>
	В чистом поле стояло тут дерево, <br>
Березонька бела; <br>
На том ли на дереве сидит птица пава. <br>
Кричит пава: «Запропала солдатская слава!»<br>
Летели гуси со святой Руси: <br>
«А знать, нам, солдатушки, не бывать на Руси, <br>
На Руси не бывать, отцов не видать, <br>
Матерей не знать». <br>
— «Солдатушки, ребятушки, а где ваши домы?» <br>
— «Наши домы — круты горы, широки раздолья, <br>
То наши подворья». <br>
— «Солдатушки, ребятушки, а где ваши жены? — <br>
«Наши жены — ружья заряжены, штыки присажоны». <br>
— «Солдатушки, ребятушки, а где ваши дети?» <br>
— «Наши дети — ложки полужёны, в сумы положоны, <br>
В поход припасёны». <br>
— «Солдатушки, ребятушки, а где ваши тетки?» <br>
— «Наши тетки — шапки теплы на буйной головке». <br>
— «Солдатушки, ребятушки, а где ваши кони?» <br>
— «Наши кони — резвы ноги, завсегда в походе, <br>
В казенной работе. <br>
Мы под Киев подходили, на горку всходили, <br>
Мы на горочку всходили, во фрунт становились». <br>
<br>
