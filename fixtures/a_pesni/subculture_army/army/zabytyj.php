  Он смерть нашел в краю чужом, <br>
  В краю чужом, в бою с врагом; <br>
  Но враг друзьями побежден, - <br>
  Друзья ликуют, только он <br>
  На поле битвы позабыт, <br>
  Один лежит. <br>
  <br>
  И между тем как жадный вран <br>
  Пьет кровь его из свежих ран <br>
  И точит незакрытый глаз, <br>
  Грозивший смертью в смерти час, <br>
  И, насладившись, пьян и сыт, <br>
  Долой летит - <br>
  <br>
  Далеко там, в краю родном, <br>
  Мать кормит сына под окном: <br>
  "А-гу, а-гу, не плачь, сынок,<br>
  Вернется тятя. Пирожок <br>
  Тогда на радостях дружку <br>
  Я испеку..." <br>
