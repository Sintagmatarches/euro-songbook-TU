<h1>КРЫЛАТЫЕ РАКЕТЫ</h1>

Вся Земля теплом согрета,<br>
 В старом парке тает лед. <br>
 И крылатые ракеты <br>
 Начинают свой полет. <br>
 Долетят они до цели<br>
 И начнутся чудеса: <br>
 Термоядерной пылью <br>
 Вдруг затянет небеса.<br>
<br>
Во всеееее концы планеты, <br>
Не ведая преград, <br>
Крылатые ракеты <br>
Летят, летят, летяяяят.<br>
<br>
<span class='i'><a href="https://www.facebook.com/alexandra.arkhipova.50/posts/2517761404918134?comment_id=2517905241570417&comment_tracking=%7B%22tn%22%3A%22R%22%7D" target="_blank">Комментарий</a> Екатерины Лапиной-Кратасюк в Facebook 11 нояб. 2018, с прим.:<br>
<br>
Мы это распевали такими ангельскими голосами во время ночных костров в КЛШ - Краевой летней школе г. Красноярск.<br>
<br>
А следующей песней обычно шла Кобзоновская.<br>

<br>
Все ближе атом - и все страшней,<br>
 Все ближе ужас разрывов грозных. <br>
 Еще не много ночей и дней - <br>
 И будет поздно, и будет поздно. <br>
 Планета крикнет: &quot;Укрой меня<br>
 От этой тучи, от смертоносной, <br>
 Укрой от этой волны огня!&quot;<br>
  Но будет поздно, но будет поздно! <br>
  <br>
  Вообще, это как-то очень весело было, а Кобзона и переделывать не нужно было - как пародия шло.<br>
<br>
А еще была вот такая - но она, кажется, все-таки про Вьетнам. <br>
<br>
Вместе весело шагать по болотам, <br>
По просторам по зеленым. <br>
И деревни выжигать лучше ротой <br>
Или даже батальоном. <br>
В небе зарево пожарищ заполощется, <br>
Раз бомбежка, два бомбежка - нету рощицы. <br>
Раз атака, два атака - нет селения. <br>
Успокоим коренное население.<br>
