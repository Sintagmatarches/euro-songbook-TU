    Песенка времен холодной войны между СССР и Западом. Навеяна, вероятно, эстрадной версией баллады американских шахтеров &quot;Sixteen Tons&quot;, которая получила мировую известность после записи ее в 1955 г. на пластинку Теннесси Эрни Фордом (Tennessee Ernie Ford). У русской песни своеобразная конструкция: обязательно есть куплет-зачин об отправке бомбить, первый припев, кумулятивный куплет из произвольного числа двустиший (обычно 3-4), в каждом из которых очередное попадание приносит разрушения, и финальный припев, зеркально отражающий первый припев. Остальные куплеты не обязательны. <br>
