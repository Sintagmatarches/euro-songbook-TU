<h1>СИДИМ МЫ В БАРЕ В ПОЗДНИЙ ЧАС</h1>

Сидим мы в баре в поздний час,<br>
И вдруг от шефа нам приказ:<br>
&quot;Летите мальчики на восток,<br>
Бомбите, мальчики, городок!&quot;<br>
А городок тот непростой,<br>
Он называется Москвой.<br>
<br>
Летим над морем - красота:<br>
Шесть тысяч метров высота.<br>
 За нами гонит русский ас,<br>
А я балдею, жму на газ.<br>
<br>
Первый снаряд попал в крыло,<br>
Радиста к черту унесло.<br>
Второй снаряд попал туалет,<br>
Там было трое, теперь их нет.<br>
Третий снаряд попал в капот,<br>
И раком встал второй пилот.<br>
Четвертый снаряд попал в багаж,<br>
И раком встал весь экипаж.<br>
<br>
Летим над морем - красота:<br>
Три сантиметра высота.<br>
Летели мы бомбить Союз,<br>
Теперь летим кормить медуз.<br>
