  <strong>ЖЕЛЕЗНОВ Иосиф Сергеевич (12.01.1888 - ?)</strong>, военный лётчик, 
  прапорщик. Родился в крестьянской семье в Тверской губернии. Окончил реальное 
  училище принца Ольденбургского (4 класса). 19 августа 1914 г. вступил в службу 
  охотником в 1 авиароту. 5 октября 1914 г. был командирован в офицерскую школу 
  авиации для обучения полетам. 22 апреля 1915 г. получил звание лётчика и 25 
  апреля назначен в 27 корпусной авиаотряд. 21 июля 1916 г. был командирован в 
  Севастопольскую авиашколу. 15 октября 1916 г. вернулся в свой отряд. В июне 
  1917 г. был переведен в Ревельский авиаотряд при морской крепости Императора 
  Петра I. За боевые отличия был награжден: Георгиевскими крестами 4-й и 3-й ст., 
  орденами Св. Анны 4-й ст. с надписью &quot;за храбрость&quot;, Св. Анны 3-й 
  ст. с мечами и бантом и Св. Станислава 3-й ст. с мечами и бантом и Георгиевским 
  оружием. С августа 1918 г. по июль 1919 г. командир 2 Олонецкого авиаотряда 
  (27 разведавиаотряда), с июля 1919 г. лётчик 26-го разведавиаотряда. 5 сентября 
  1919 г. попал в Лбищенске в плен к уральцам. Лётчик 10-го авиаотряда. Перелетел 
  к красным в ноябре 1919 г., за что был награжден орденом Красного знамени. <br>
