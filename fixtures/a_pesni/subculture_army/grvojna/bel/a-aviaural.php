  <strong>КАПЛИН Михаил (22.11.1888 - ?).</strong> Военный лётчик, войсковой старшина. 
  Родился 22 ноября 1888 г. в гор. Уральске в семье губернского секретаря Уральского 
  казачьего войска. Окончил Оренбургский Неплюевский Кадетский корпус, Николаевское 
  Инженерное училище по 1-му разряду и Севастопольскую авиашколу с званием &quot;военный 
  лётчик&quot; (26 сентября 1915 г. по 21 июля 1916 г.). С 21 июля 1916 г. по 
  октябрь 1917 г. командир 6-го корпусного авиаотряда. Был награжден орденами: 
  Св. Станислава 3-й ст., Св. Станислава 2-й ст. с мечами и бантом, Св. Анны 4-й 
  ст. с надписью &quot;за храбрость&quot;, Св. Анны 3-й ст. с мечами и бантом, 
  Св. Владимира 4-й с. с мечами и бантом. 10 октября 1917 г. был командирован 
  в Петроград в Николаевскую академию для прохождения сокращенного курса. В декабре 
  1917 г. отбыл из Академии в отпуск в г. Уральск. 1 февраля 1918 г. зачислен 
  на льготу в Уральское казачье войско; 14 марта 1918 г. назначен начальником 
  Пионерного отряда; с 16 марта по 21 апреля 1918 г. начальник Головного отряда 
  на ст. Семиглавый мар; с 21 апреля по 2 мая 1918 г. с Головным отрядом в составе 
  войска Шиповского фронта; 2 мая 1918 г. возобновил службу в Авиации; с 1 июня 
  1918 г. командир Уральского (с октября - 9-го авиаотряда) авиаотряда. С 26 декабря 
  1918 г. начальник Воздушного флота Юго-Западного фронта. В 1919 г. Начальник 
  Воздушного Флота Оренбургской армии. <br>
