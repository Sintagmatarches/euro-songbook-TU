  Краткие биографии лётчиков, служивших в Уральской авиации в 1918-1919 гг. </strong><br>
  <br>
  <strong>АРАКЕЛОВ Арзас. </strong>Военный лётчик, подпоручик. С 14 ноября 1918 
  г. был прикомандирован к Авиационному парку Добровольческой армии. С 28 апреля 
  по 4 июня 1919 г. в составе 2 взвода Офицерской Строевой Авиационной роты воевал 
  на Манычском фронте. С 7 июня 1919 г. летчик 10 авиаотряда Добровольческой армии. 
  С 1 апреля 1920 г. был зачислен в резерв летчиков при Управлении Начальника 
  Авиации ВСЮР. 15 апреля 1920 г. был уволен от службы &quot;как уроженец Закавказья, 
  для поступления на службу в армию Армении&quot;. <br>
  <br>
  <strong>фон БЕЙЕР Сергей Владимирович.</strong> Военный лётчик, прапорщик. Окончил 
  Севастопольскую авиашколу. Лётчик 1 авиапарка. С ноября 1916 г. лётчик Гренадерского 
  авиаотряда. С января 1919 г. лётчик 30-го разведывательного авиаотряда Красного 
  воздушного флота. 5 сентября 1919 г. попал в плен к уральцам в Лбищенске. Служил 
  лётчиком в 10 авиаотряде Добровольческой армии. В начале 1920 г. будучи больным 
  тифом, попал в плен к &quot;красным&quot; в г. Гурьеве. Продолжал служить в 
  Красном воздушном флоте. Умер в Советской России. <br>
  <br>
  <strong>БЛУМЕНФЕЛЬД (иначе Блюменфельдт) Герберт Яковлевич.</strong> Морской 
  лётчик, штабс-капитан. Вероятно, он был сыном капитана дальнего плавания О.А. 
  Блуменфельда парохода «Императрица Александра». Писарь 1 статьи 2 Балтийского 
  флотского экипажа. 2 августа 1915 г. прибыл для учебы в Петроградскую школу 
  морских лётчиков. 24 октября 1917 г. был произведен в подпоручики по адмиралтейству. 
  Приказом Главнокомандующего войсками и флотом Кавказа генерал-майора Л. Бичерахова 
  поручик Блуменфельд произведен в штабс-капитаны по адмиралтейству с 1 августа 
  1918 г. 23 ноября 1918 г. приказом командира Бакинского порта за №3 морской 
  лётчик подпоручик (!) Блуменфельд объявлен начальником Отдела гидроавиации. 
  6 декабря 1918 г. в связи с объединением должностей начальника гидроавиации 
  и начальника Отдела гидроавиации стал начальником гидроавиации Каспийского моря. 
  В середине июня 1919 г. бывшего заведующего гидроавиашколой (Бакинской школой 
  морской авиации) лейтенанта Блуменфельда командировали в Гурьев вместе с гидроаэропланами 
  для организации там гидроавиаотряда. С августа по декабрь (?) 1919 г. — начальник 
  гидроавиационного отряда Уральской армии в чине штабс-капитана. В мае 1921 г. 
  был в лагере в Басре в Месопотамии. <br>
  <br>
  <strong>ВЕСЕЛОВСКИЙ Валентин Семенович (1891 - ?).</strong> Военный лётчик, 
  подполковник. Родился 19 мая 1891 г. в семье потомственного почётного гражданина 
  г. Каменец-Подольсккий. Окончил среднее сельхоз. училище. 11 октября 1914 г. 
  зачислен в 1 авиароту охотником. 23 декабря 1914 г. окончил теоретические курсы 
  ОВФ при петроградском политехническом институте. 25 сентября 1915 г. окончил 
  курс обучения полётам в Севастопольской авиашколе и переведен лётчиком 12-й 
  корпусной авиаотряд. В 1916 г. окончил Одесскую авиашколу и 14 января 1917 г. 
  был удостоен звания &quot;военного лётчика&quot;. С 31 марта 1917 г. лётчик 
  8-го армейского авиаотряда. За боевые отличия был награжден: Георгиевскими крестами 
  4-й и 3-й ст., Георгиевскими медалями 4-й и 3-й ст., орденами Св. Владимира 
  4-й ст., Св. Станислава 3-й ст. и 2-й ст., Св. Анны 3-й ст. С октября 1918 г. 
  до февраля 1919 г. командир 1-го авиаотряда Южной армии. В 1919 г. в резерве 
  чинов при Управлении Начальника авиации Донских армий. С 14 июля по 5 сентября 
  1919 г. был в командировке по установлению связи со штабами Уральского и Оренбургского 
  войск. Приказом №01908 Уральской Отдельной армии от 16 августа 1919 г. капитан 
  Веселовский был награжден войсковым орденом Св. Архистратига Михаила (единственный 
  из летчиков, воевавших на стороне уральцев, получивший эту награду). 23 мая 
  1920 г. был прикомандирован к Управлению Начальника Авиации ВСЮР. 24 августа 
  1920 г. переведен в резерв лётчиков при Управлении Начальника Авиации Русской 
  армии. 20 сентября 1920 г. был переведен на солдатскую должность во 2 авиапарк. 
  Эмигрировал в Румынию. <br>
