	<br><span class="text"><span class="header"><strong>СКАУТИЗМ 
        НА ДОНУ И КУБАНИ В ГОДЫ ГРАЖДАНСКОЙ ВОЙНЫ</strong><br></span>
  <br>
  Материалы предоставлены Р. Полчаниновым<br>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 72-75</em>.<br>
  <br>
  <br>
  <strong>Клименко В.А.* <br>
        Скаутизм на Дону</strong><br>
  <br>
  На рассвете 23 апреля 1918 г. Новочеркасск был взят казаками, и перепившийся 
  гарнизон красных беспорядочно отступал к Хутунку. Небольшие группы красных (все 
  в касках) растерянно шарахались и всюду натыкались на занявших город казаков. 
  У Хутунка разгорелся сильный бой. Было около восьми часов утра. По обе стороны 
  Кавказской улицы пробирался взвод одной из сотен Новочеркасского партизанского 
  полка, под прикрытием стен домов... <br>
  <br>
  Уже почти у самого угла с Атаманским спуском, небольшая группа красных напоролась 
  на партизан. После нескольких взрывов ручных гранат и выстрелов, на середине 
  улицы остался лежать один убитый красный, и длинный кровавый след потянулся 
  вверх по Атаманскому спуску... Выше опять затрещали выстрелы. Это, кажется, 
  были последние выстрелы на улицах города. Было прекрасное весеннее утро. Ведь 
  была Св. Пасха! <br>
  <br>
  Появился верхом на лошади офицер: на шинели чернильным карандашом были нарисованы 
  погоны, просвет и две звездочки. Хорунжий разыскивал начальника скаутов» (так 
  и сказал), чтоб отдать приказание Походного атамана. Петроградский инструктор 
  вышел и подошел к офицеру. <br>
  <br>
  Распоряжение было не из легких: генерал Попов приказывал немедленно собрать 
  всех скаутов, как можно скорее и, захватив по дороге в любой аптеке перевязочный 
  материал, бежать к Хутунку! Были раненые, но не было ни сестер милосердия, ни 
  фельдшера. Кроме того, была нужна быстрота. <br>
  <br>
  Налаженные знакомства и контакт — пригодились. Не прошло и 15-20 минут, как 
  были собраны 3 мальчика и 7 девочек (старшей было 15 лет), с которыми и побежал 
  инструктор, забегая на ходу в аптеки... <br>
  <br>
  Столица Дона расположена на горе, и чтобы сбежать к Хутунку, нужно пройти площадь, 
  с которой раскрывается вид, как на ладони, и на Хутунок, и на железнодорожный 
  мост, и на возвышенность с левой стороны города. Здесь именно и была задержана 
  скаутская группа. Командир батареи, стоявшей здесь же на позиции, тоном, не 
  допускающим возражений, приказал бежать тушить пожар на хуторе, дым с которого 
  закрывал ему всю даль... Сюда же подошла одна сестра милосердия, и за ней шагом 
  ехали две подводы с ранеными. Нужен был перевязочный материал. Думать было некогда. 
  Раненых сняли с двух подвод. На подводах разместились быстро скауты и сестра 
  с каким-то человеком (вероятно, санитар) и поскакали во всю прыть к хутору, 
  из которого валили густые черные клубы дыма, заволакивая все небо. Но как и 
  чем тушить пожар? Горело зерно. <br>
  <br>
  Один из основных навыков скаутов — не теряться в самой невозможной обстановке! 
  Так получичилось и здесь. Оказалось, что тушить горящее зерно совсем не сложно. 
  Несколько бродивших по улице и во дворах жителей приняли живое участие в тушении 
  и, когда через час приблизительно прибыла пожарная команда, ни дыма, ни огня 
  уже и в помине не было! Только начальник, опытный брандмейстер, все никак не 
  мог понять, как это «девочки тушат пожар!» <br>
  <br>
  Любителям же батальных картин, можно рассказать следующее: в момент перемены 
  задания и разговора с командиром батареи, скауты были свидетелями исторического 
  момента. Это был самый тяжелый момент, когда бронепоезд красных начал подходить 
  к железнодорожному мосту через Тузлов. На поле, вблизи от полотна, лежала цепь 
  Заплавской станицы и... дрогнула. Еще бы! Бронепоезд поливал ее из пулеметов! 
  Момент был тяжелый! <br>
  <br>
  Вот тогда из-за бугра появилась конница с одним орудием, которое, снявшись с 
  передка, дало несколько выстрелов по бронепоезду. Это были подошедшие Дроздовцы... 
  Таким образом, Донские скауты были свидетелями того, что не забыли, наверняка, 
  никогда. А гордились они — исполнением приказания. <br>
  <br>
  Дело этим, конечно, не кончилось. Генерал Попов приказал формировать скаутов, 
  и этим было положено начало первой государственной организации на Русской Земле. 
  Много лет прошло с тех пор, но понимание Донских атаманов настоятельной необходимости 
  здорового и патриотического воспитания молодежи только подчеркивает знание государственных 
  удач. <br>
  <br>
  О начале, развитии и конце Организации Бой и Герл Скаутов на Земле Всевеликого 
  Войска Донского стоит поговорить, хотя бы уже потому, что, вопреки всей неурядице 
  и безобразному устройству тылов на всем Юге России - Дюн был единственным «оазисом» 
  в разбушевавшейся стране, где были порядок, закон и устремление к государственному 
  возрождению. Более того, говорят, что юношеские организации являются как бы 
  фотографией и своей страны, и своего народа. Слава Богу, Донским скаутам нечего 
  смущаться или краснеть! <br>
  <br>
  Приказ Походного атамана был короток и ясен: формировать Донских скаутов! Маленькая 
  группа, окрыленная своим успехом при взятии Новочеркасска, решила начинать вербовку 
  своими средствами. Но петроградский инструктор понимал дело совершенно иначе. 
  Раз приказ исходил от власти — нужно было действовать в широком масштабе!.. 
  <br>
  <br>
  Главная трудность заключалась в том, каким путем сделать большую и публичную 
  огласку новой организации, при отсутствии средств, знания местных условий и 
  без нужных знакомств. Но молодой инструктор уже имел в Петрограде практику по 
  организации отряда при сестрорецком яхт-клубе и при 12-ой Петроградской гимназии, 
  а донских казаков знал с детства (по Польше), по 6-му Донскому казачьему генерала 
  Краснощекова полку! Присутствие же молодых офицеров Добровольческой армии — 
  натолкнуло на мысль об устройстве танцевального вечера на Детской площадке, 
  приспособленной для киносеансов в городском саду. Конечно, настоящей цели своего 
  начальника дети не знали... Все с энтузиазмом взялись за дело. <br>
  <br>
  Этими спектаклями заведывало благотворительное общество, и только после категорического 
  отказа от кассы и какого бы то ни было барыша, председатель (генерал в отставке) 
  соблаговолил дать разрешение. <br>
  <br>
  Представилась и другая трудность: необходимо было построить помост с настилом 
  досок для танцев, но и это нашлось на складе. Был нанят пьянчужка плотник и... 
  работа закипела. Все устроилось, как нельзя лучше, а последнее средство — имя 
  Походного атамана — убеждало самых инакомыслящих... <br>
  <br>
  Расклеенные афиши по всему городу собрали все молодое и веселое. Чудная майская 
  ночь при свете электричества и фонариков, создавала обстановку уже давно позабытую. 
  Очень хороший оркестр играл великолепно. Все были в восторге, особенно девочки-скаутки, 
  тушившие пожар! <br>
  <br>
  Еще до начала инструктор условился с оркестром, что по его сигналу сыграют туш, 
  и он выступит с «заявлением». Расчет организатора всего этого был прост: нужно 
  было публично объяснить всем, что представляет собой новая организация. Кроме 
  того, предполагалось, что масса молодежи (старшей) разнесет эту новость, и, 
  таким образом, будет сделана нужная пропаганда (как говорят теперь!)... Трудно 
  сейчас вспомнить, как и чем начал свою речь молодой энтузиаст. Воцарилась тишина. 
  Все были удивлены. Но, по мере того, как говорил инструктор, а говорил он от 
  души и чистого сердца — нарастал энтузиазм у публики. Дружные аплодисменты превратились 
  в настоящий триумф. Какой-то старичок генерал схватил юного оратора в свои объятия 
  и взволнованным голосом говорил: «Таких, такую молодежь нам нужно теперь. От 
  всего сердца желаю вам и вашим успеха!» <br>
  <br>
  Ну, а сам-то виновник торжества, о чем думал?.. <br>
  <br>
  В конце свой речи он подчеркнул, что с утра следующего дня открыта запись новичков, 
  здесь же, на Детской площадке. Все теперь сводилось к простому, но самому главному: 
  откликнутся на призыв или нет? Но расчет оказался правильным, и задолго до 9 
  часов утра следующего дня у входа на Площадку стояла толпа учеников и учениц 
  всех учебных заведений Новочеркасска! Назначенные для записи сбивались с ног, 
  а некоторые, кто не желал ждать, уходили в гимназию Петровой, о чем позаботились 
  скауты другого петроградского скаутмастера. С этого дня и начался антагонизм 
  среди двух главных подразделений скаутов в этом городе. <br>
  <br>
  Не будем уже входить в подробности, как дело развивалось, как приходилось создавать 
  все из ничего. Прежде всего, необходимо было найти подходящего человека, чтоб 
  возглавить обе враждующие стороны и прекратить вредный антагонизм. Сказывалась 
  молодость обоих инструкторов. В те времена и верные своей традиции инструктора 
  были из той же учащейся молодежи, в особенности, в Петрограде. При наличии старшего 
  по возрасту начальника, была светлая надежда прекратить совершенно вражду и 
  критику... <br>
  <br>
  По инициативе позже приехавшего, был найден человек — капитан дальнего плавания 
  Александр Петрович Дехтерев. Трудно было найти во время войны, да еще в той 
  сумбурной обстановке, когда все лучшее было на фронте, человека подходящего. 
  Выбор был сделан поспешно и, как показало будущее, неудовлетворительно! А, может 
  быть, это была судьба того смутного и ненормального времени? Думаем, что оба 
  инструктора понимали опасность положения и... поторопились. А антагонизм, как 
  будто прекратился. Новый же начальник сразу решил сформировать организацию, 
  которая и была вскоре официально утверждена атаманом Красновым. Дело спорилось... 
  <br>
  <br>
  Приятно было читать в газетах приказы по Гарнизону о парадах: «...и 1-ой и 2-ой 
  Дружинам Бой и Герл скаутов на Земле Всевеликого Войска Донского». Назначались 
  Инспекторские смотры в Краснокутской Роще, которые посещал Донской атаман. Выдан 
  был шанцевый инструмент и палатки. Выдано было много материала для формы. Проведены 
  были штаты, и начальники дружин получали казенное содержание. Одним словом, 
  это уже была серьезная работа, и можно было предполагать, что Организация выльется 
  в могучее детище многих русских патриотов и деятелей, которые только и мечтали 
  о «русификации» нашего скаутизма с давних пор (еще до революции), отдавая себе 
  отчет в том, что русским детям нужно преподавать все русское и базироваться 
  на нашем же историческом прошлом. Такими были в истории Русского скаутизма: 
  адмирал И.Ф. Бострем, Преображенский, Попов и многие-многие другие. <br>
  <br>
