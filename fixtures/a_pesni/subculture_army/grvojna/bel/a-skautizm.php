  <br>
        <strong>О чем нельзя забыть (Из воспоминаний бывшего старшего скаута Дона) 
        </strong><br>
  <br>
  1918 и 1919 годы я провел на Дону. Будучи старшим скаутом Дона, организуя в 
  городах и станицах дружины и отряды мальчиков и девочек — скаутов, я невольно 
  был свидетелем исключительных дел, превосходящих всякое представление о возможности 
  массового подвига, когда дети, забывая об отдыхе, играх, еде, все свободное 
  от учебных и домашних занятий время, проводили в лазаретах, на питательных пунктах 
  и в учреждениях, заменяя санитаров и технических служащих, так как обслуживающих 
  людей не хватало: фронт поглощал всех, кто был здоров. <br>
  <br>
  Здесь в расчет не брались ни праздники, ни даже ночи. И часто мальчики возвращались 
  домой из лазарета домой только утром, и оттуда шли в школу. Главная забота их 
  была — служить страждущему воинству, и это получалось у них так естественно, 
  силы их были так нужны, что никому и в голову не приходило этому препятствовать. 
  <br>
  <br>
  Многие мальчики ушли на фронт, не удовлетворяясь жертвенным подвигом в тылу, 
  унесшим в могилу немало детей. Особенно помню милого, очень глубокого мальчика 
  Сережу Кочетова, ухаживавшего за больными, заразившегося сыпным тифом и буквально 
  сгоревшего в несколько дней. На фронте были тяжело ранены Коля Горев, Игорь 
  Автократов и другие, им же числа нет. Много детей было убито, в особенности 
  в отрядах есаула Чернецова и генерала Семилетова. Припомнить их имена я не в 
  силах, так как все записи того времени покоятся в недрах земли между Новороссийском 
  и Геленджиком, да и самих детей было в Организации до семи тысяч (в десятке 
  городов и станиц). <br>
  <br>
  В январе 1920 года почти все скауты, достигшие 14-15 лет, вошли в состав Армии. 
  О нечеловеческих муках их мы едва ли когда-нибудь узнаем, могилы их вряд ли 
  когда-нибудь найдем. В этом весь ужас всех гражданских войн от зари человечества 
  до наших дней. Оказавшись с родителями за границей, дети-скауты тоже не посрамили 
  себя, оказав много услуг страждущему воинству, вывезенному в 1920 году генералом 
  Врангелем из Крыма. <br>
  <br>
  Вот краткие выписки из моего дневника того времени: <br>
  <br>
  13 ноября 1920 г. — Остров Халки... Крыма нет. Сиваш замерз. Небывало ранняя 
  зима принесла раннюю смерть. <br>
  <br>
  16 ноября 1920 г. — Константинополь. Ранним утром я отправился в Константинополь. 
  Путь был - сквозь строй — путем Голгофы. Более 50 наших пароходов стояли на 
  рейде, до отказа переполненные эмигрантами. <br>
  <br>
  Эти пароходы трагически высились по всему нашему пути. Здесь не менее ста тысяч 
  войск и гражданского населения. Нашего военного флота пока еще нет, он задержан 
  у входа в Босфор. Ближе к пристани стоят иностранные суда, вернувшиеся из портов 
  Крыма. В сторону Кавак виднеется мощная англо-французская военная эскадра. И 
  весь этот сонм морских чудовищ завуалирован легким туманом, точно траурным крепом. 
  <br>
  <br>
  В витрине Бюро Русской печати выведены следующие строки: «Русские люди, помогите. 
  Беженцы, прибывшие из Крыма, крайне нуждаются в продовольствии. Помогите, кто 
  чем может, хотя бы караваем хлеба. Всякие пожертвования немедленно будут отправлены 
  на пароходы, где каждый кусок хлеба будет принят с благодарностью. Пожертвования 
  деньгами и продуктами принимаются здесь в течение целого дня». <br>
  <br>
  Из Бюро я отправился в Русский Маяк, где председательствовал в Совете Главной 
  Квартиры Скаутов. Мой проект незамедлительной помощи беженцам прошел и сегодня 
  же начнет проводится в жизнь. <br>
  <br>
  20 ноября 1920 г. — Во дворце Русского Посольства толчея. Привезли с пароходов 
  раненых и больных. Более десяти лазаретных машин таят в себе муку и печаль. 
  А грузовики прибывают еще и еще. Часть скаутов моют полы в помещении Посольства, 
  часть - набивают соломой матрасы и устанавливают койки, часть — в белых халатах 
  переносят раненых. Только их волей и их руками оборудован весь огромный лазарет. 
  Скауты мыли помещения, добывали и привозили инвентарь, привозили провиант, держали 
  связь с пароходами и различными учреждениями. Таким образом работало более ста 
  мальчиков и юношей, принадлежащих русским дружинам в Константинополе и на Принцевых 
  островах. Все наш детские силы были мобилизованы. <br>
  <br>
  Уходя из посольства, я поблагодарил скаутов за тяжелый самоотверженный труд. 
  <br>
  <br>
  Во дворе — столпотворение вавилонское. Здесь и военные в разнообразнейших формах, 
  и штатские. Многие уже запаслись чашками с дымящимся кофе. Лица их сияют блаженством. 
  Тут же из грузовиков выносят тяжело раненых. Один из них, мерно колыхаясь на 
  носилках, крепко прижал к своей груди хлеб. Наголодался, видно... Грязные пальцы 
  его судорожно впились в мякиш... <br>
  <br>
  ...И разве все это? На тему о героизме, о самопожертвовании детей можно написать 
  не одну книгу. Мне бы очень хотелось, чтобы мальчики патруля, побывавшего в 
  Англии на Джембори, прочли эти строки. Пусть они еще выше поднимут свой родной 
  трехцветный флаг. И голову выше: ведь эти русские скауты — братья тех скаутов-героев, 
  о которых здесь была речь. И еще я хочу: чтобы Россия не забыла русских мальчиков, 
  на деле показавших пламенную любовь к ней. <br>
  <br>
  <em>За правду. Буэнос-Айрес. 1949 г. №34. </em><br>
  <br>
  <br>
        <strong>Атаманский скаут. Разведчество на Кубани</strong><br>
  <br>
  Впервые скаутские отряды возникли на Кубани в годы войны (1915-1916 гг.) в городах 
  Армавире, Екатеринодаре и в прилегающих к Кубанской области Ставрополе и Минеральных 
  Водах. <br>
  <br>
  Огромное влияние оказали на развитие разведчества прибывшие на территорию Северного 
  Кавказа скауты — военные беженцы из Риги, Ревеля, Пернова и Петрограда, дав 
  толчок к еще большему расширению деятельности существующих единиц и созданию 
  новых, в частности в Лабинской, Майкопе, Туапсе, Новороссийске и в некоторых 
  пунктах Черноморого побережья (Анапа, Сочи).<br>
  <br>
  Между всеми возникшими организациями быстро наладилась связь. Контакт был налажен 
  также с главными российскими центрами: Петроград (И.Н. Жуков), Москва (В.А. 
  Попов), Киев (А.К. Анохин), Одесса (Е.Ф. Молчанов) и т.д. <br>
  <br>
  Волна начавшегося коммунизма не только не смела скаутские организации на Кубани, 
  но еще теснее их сплотила. <br>
  <br>
  После разгрома большевиков и создания белой власти на юге России, скаутские 
  организации Кубани в 1918-1919 гг. расцветают пышным цветом. <br>
  <br>
  Прибытие на Кубань Эраста Платоновича Цитовича (казака ст. Вознесенской, долгие 
  годы занимавшего пост директора Царскосельской реальной гимназии, и в последние 
  годы бывшего начальником 1-го Царскосельского отряда русских скаутов), занимавшего 
  тогда пост министра просвещения Кубанского Краевого Правительства, дает большой 
  толчок к еще большему расцвету и единению. <br>
  <br>
  Возглавив Кубанский скаутизм, Э.П. Цитович обращает внимание на укрепление казачьего 
  духа в Кубанской организации. Первым его требованием была организация практической 
  помощи городских отрядов станичным, с максимальным втягиванием туда станичных 
  казачат. Отряды скаутов создаются в станицах Невинномысской, Прочноокопской, 
  Вознесенской, Белореченской, Кавказской (хут. Романовский), Усть-Лабинской, 
  Пашковской, Ейске и ряде других. <br>
  <br>
  Для поддержания старых казачьих традиций всей Кубанской организации дается старинное 
  казачье название «громада» (Громада Кубанских Скаутов). Вводится, как высшая 
  награда, соответствующая английскому званию «королевского скаута», звание «атаманский 
  скаут». <br>
  <br>
  Оформляется руководящий центр Организации. Под редакцией Э.П. Цитовича издается 
  книга «Русский Скаут», а под редакцией Л.П. Иванова — «Руководство для скаутов 
  и скаутских дружин». <br>
  <br>
  Завязывается братская связь с соседним Всевеликим Доном, где на смену скаутмастеру 
  Войту, старшим скаутом Дона становится скаутский деятель, капитан дальнего плавания 
  А.П. Дегтярев. <br>
  <br>
  Особенно интенсивно развивалась деятельность донских скаутов в Новочеркасске, 
  Ростове, Таганроге и ряде станиц. Полностью восстанавливается прерванная Гражданской 
  войной связь Кубани со Ставрополем, Минеральными Водами, Новороссийском и Туапсе. 
  Доходят вести о работе братских скаутских организаций в Киеве, Елизаветграде, 
  Екатеринославе, Харькове, Одессе и даже Сибири (Омск, Чита, Владивосток). <br>
  <br>
  Кульминационным пунктом расцвета кубанской организации скаутов является проведенный 
  в г. Армавире в мае 1919 г. слет Кубанской Громады. Помимо одиночных представителей 
  из разных городов (Туапсе, Ставрополь, Новороссийск, Минеральные Воды), на слет 
  прибыли в железнодорожных теплушках целые отряды из станиц: Лабинской, Белореченской, 
  Невинномысской, Кавказской и Пашковской, что вместе с отрядами Армавирским, 
  Вознесенским и Прочноокопским, составило довольно внушительное зрелище. Возглавлял 
  слет прибывший в Армавир Э.П. Цитович. <br>
  <br>
  Последовавшие вскоре отход Белых армий и эвакуация большинства разведчиков и 
  руководителей, отразилась на работе Кубанских скаутских отрядов, но, как ни 
  странно, они не распались, а продолжали повсеместно свою работу в подполье. 
  Они создавали новые формации, восстанавливали прерванные связи, искали выходов 
  из создавшегося положения. Завуалировавшись знаком СОВа (Союз Возрождения Скаутизма 
  на Юге России), действовал Северо-Кавказский центр в контакте с центрами: Донским 
  (Организация Донских Скаутов, под руководством старшего скаутмастера Н. Маслюкова) 
  и Московским (Организация старших скаутов «Братья костра», под руководством 
  скаутмастера Н. Фатьянова). <br>
  <br>
  Пронесшаяся над Доном и Кубанью волна советских репрессий закончила деятельность 
  скаутизма на Кубани. Мало кто спасся от ужасов красного террора, охватившего 
  бывшие Белые территории Юга России. Естественно, что казачьи области Дона и 
  Кубани подверглись наиболее жестокому террору красных пришельцев. <br>
  <br>
  И много лет спустя, отдельные оставшиеся скауты, хотя и выросшие в новых советских 
  условиях, но не забывшие своего пребывания в Громаде, еще долго вспоминали славные 
  годы расцвета Кубанского скаутизма. <br>
  <br>
  Доказательствами того служат найденные советами, любовно и тщательно скрытые 
  до 1941 года: Волчье знамя генерала Шкуро, подаренное им в 1918 г. ставропольским 
  скаутам в день занятия города, голубое Георгиевское знамя Армавирской Дружины, 
  Зеленое знамя Лабинского отряда, Донские знамена, печати и реликвии, оставленные 
  при эвакуации 1920 г. в г. Новороссийске. <br>
  <br>
  <em>Свисток. Сан-Франциско. 1953. 01.02. №29. </em><br>
