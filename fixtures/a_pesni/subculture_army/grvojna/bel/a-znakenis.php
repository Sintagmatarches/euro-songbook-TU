	<br><span class="text"><span class="header"><strong>ЗНАК ЕНИСЕЙСКОЙ КАЗАЧЬЕЙ БРИГАДЫ </strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 264</em>.<br>
  <br>
  <br>

  <img src="znak-enis.jpg" width="158" height="231"><br>
  <br>
  <br>
  Приказ <br>
  по Военно-морскому ведомству <br>
  №6 <br>
  19 октября 1921 г. <br>
  Кр[епость] Владивосток <br>
  <br>
  В память самоотверженной боевой работы Енисейской казачьей бригады, вынесшей 
  всю тяжесть борьбы с большевиками с 1918 года, устанавливается Знак Енисейской 
  Казачьей Бригады, общий для офицеров и казаков. <br>
  <br>
  На ношение Знака «Енисейской Казачьей Бригады» имеют право все господа офицеры 
  и казаки, поступившие в Енисейскую казачью бригаду не позднее 1 января 1919 
  года, перешедшие озеро Байкал и границу Китайской Республики в составе части, 
  не исключая больных и раненых, эвакуированных в полосу отчуждения из частей 
  бригады во время пребывания в Забайкалье.<br>
  <br>
  Все офицеры и казаки, имеющие право на ношение вышеуказанного знака, должны 
  получить на это удостоверение за подписью Командира 2-го стрелкового корпуса 
  по представлению Атамана Енисейского казачьего войска (без проведения сего приказом).<br>
  <br>
  Списки всех лиц, имеющих право на знак «Енисейской казачьей бригады» хранятся 
  в Штабе 2-го стрелкового корпуса, в Штабе Енисейского полка и в Управлении по 
  делам казачьих Войск.<br>
  <br>
  Во избежание злоупотреблений и в целях предупреждения незаконного ношения «Знака 
  Енисейской казачьей бригады», лицам, на то права не имеющим, предоставить «Обществу 
  офицеров Енисейского казачьего полка» право входить по сему вопросу с представлением 
  по начальству для привлечения виновных к ответственности.<br>
  <br>
  В случаях спорных, вызывающих сомнения о праве того или иного лица на «Знак 
  Енисейской казачьей бригады» Обществу офицеров Енисейского казачьего полка предоставляется 
  право окончательного разрешения вопроса в положительном или отрицательном смысле.<br>
  <br>
  «Знак Енисейской казачьей бригады» носится выше всех знаков до выхода новых, 
  как позднейший по своему установлению.<br>
  <br>
  Офицеры и казаки, имеющие право на «Знак Енисейской казачьей бригады», никакими 
  особыми по сей причине правами и преимуществами не пользуются и приобретают 
  знак собственным попечением без пособия от казны.<br>
  <br>
  Лишаются права на знак «Енисейской казачьей бригады» все офицеры и казаки, уволенные 
  от службы в порядке дисциплинарном и по судебным приговорам.<br>
  <br>
  Приложение: Описание знака.<br>
  <br>
  Подлинный подписал: <br>
  Управляющий Военно-морским ведомством, генерал-лейтенант Вержбицкий<br>
  <br>
  С подлинным верно: <br>
  Обер-офицер для делопроизводства, подпоручик Макаров<br>
  <br>
  <br>
  Приложение к приказу по <br>
  Военно-морскому ведомству <br>
  от 19 октября 1921 г. №6 <br>
  <br>
  ОПИСАНИЕ ЗНАКА ЕНИСЕЙСКОЙ КАЗАЧЬЕЙ БРИГАДЫ<br>
  <br>
  Девиз «За освобождение России» <br>
  <br>
  Крест белой эмали с золотым обрезом, для казаков — бронзовый, суживающийся к 
  середине, длиною 1 1/3 дм, перекладина длиною 1/8 дм, делит высоту креста: вверх 
  1 часть и вниз 2 части, ширина концов креста S дм.<br>
  <br>
  Двуглавый Орел, серебряный, оксидизированный. Размер крыльев 1S дм. В ногах 
  Орла подкова оксидизированная.<br>
  <br>
  На крыльях Орла накладные золотые даты, на правом крыле — 1918 и на левом — 
  1921 г. На подкове золотые накладные буквы «Е.К.Б.». На груди Орла щиток с национальным 
  флагом. Крест пересекает две шашки казачьего образца, длиною каждая по 1 s дюйма.<br>
  <br>
  Подлинное подписал: <br>
  Дежурный генерал, генерал-майор Нейзель <br>
  И.д. начальника инспект[орского] отделения, капитан Аврамов<br>
  <br>
  С подлинным верно: <br>
  Обер-офицер для делопроизводства, подпоручик Макаров<br>
  <br>
  <br>
  №104 <br>
  23 декабря 1921 г. <br>
  Кр[епость] Владивосток<br>
  <br>
  В изменение приказа по Военно-морскому ведомству от 19 октября с.г. №6, ввиду 
  сформирования 1-го Сводно-казачьего корпуса, подписание удостоверений на право 
  ношения «Знака Енисейской казачьей бригады» возлагается на Командира 1-го Сводно-казачьего 
  корпуса.<br>
  <br>
  (По Управлению Дежурного генерала) <br>
