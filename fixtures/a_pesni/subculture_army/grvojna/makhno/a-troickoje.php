  Брославская</strong><br>
  <br>
  <span class="header"><strong>ТРОИЦКОЕ ВОССТАНИЕ</strong><br></span>
  <br>
  <em>"Провинциальная газета", приводится по перепечатке в газете "Казачий взгляд", 
  №4 (100), апрель 2006 г.</em><br>
  <br>
  <br>
  Это была одна из самых драматических страниц в истории Лабинского казачества. 
  Нашему архивариусу Дине Халуевой, которая работала с архивными материалами 1917-1924 
  гг. удалось установить, что 5 июня 1918 года в ст. Лабинской погибли 147 человек, 
  в том числе солдат 93 человека, казаков 17, одна медсестра, 24 мирных жителя. 
  110 человек захоронены в трех братских могилах около Свято-Успенской церкви. 
  А всего во время Троицкого восстания погибло боле 500 казаков станиц Лабинской, 
  Чамлыкской, Владимирской, Вознесенской, Упорной. <br>
  <br>
  Революция, свершившаяся в октябре 1917 года, на Кубани была встречена довольно 
  спокойно. Казаки, болезненно пережившие отречение государя Российского Николая 
  II, можно сказать, с удовлетворением отнеслись к тому, что правительство Керенского 
  было изгнано. Ведь именно казаки пострадали от "демократического" правительства, 
  которое посягнуло на их имущественные и социальные права и разрушило вековой 
  патриархальный уклад жизни. Но и свое отношение к Советской власти казаки не 
  спешили определять. Тем более, что в Советах большей частью были иногородние, 
  бедняки, которых считали бездельниками. В Лабинской и окрестных станицах происходили 
  бесконечные митинги: собираются, начинают обсуждать нужна советская власть им 
  или не нужна, спорят, ругаются до хрипоты, и все заканчивается дракой. Казаки 
  между собой рассуждали примерно так: что это за Советы все равно не поймешь, 
  лишь бы новая власть землю не отобрала, да семью не обидела. <br>
  <br>
  Но с новой властью пришла в станицы и новая жизнь, непонятная казаку. Головы 
  подняли те, кто еще недавно считался станичной голытьбой, которой представился 
  случай свести счеты с зажиточными казаками, казнить и миловать по собственному 
  усмотрению. А тут еще новые, неслыханные слова "контрреволюция", "экспроприация", 
  "ЧК", "трибунал". Сидят казаки по хатам и вроде как не хозяева своему добру, 
  потому что в любой момент могут нагрянуть из Совета и объявить о "революционной 
  экспроприации". И пошли казачьи волнения. <br>
  <br>
  В январе-феврале 1918 года происходит первое выступление казаков ст. Лабинской, 
  к которому присоединилось казачество других станиц Лабинского отдела. Но выступление 
  было стихийным - без руководителя, без определенных целей. Просто решили двинуться 
  к Армавиру, чтобы изгнать большевиков из административного центра и крупного 
  железнодорожного узла. Но на подступах к Армавиру казаки натолкнулись на превосходящие 
  силы красных, которые заставили их отступить и рассеяться. После этого последовали 
  аресты и репрессии в отношении казаков. <br>
  <br>
  В феврале 1918 года произошло восстание казаков ст. Вознесенской. Они отказались 
  признать Советскую власть и призвали на станичном сборе поддержать Раду, войскового 
  атамана, а также вооружаться и с оружием в руках защищать свои права. Казаки 
  восстановили станичное правление, которое продержалось считанные дни. Уже 1 
  марта красные при поддержке артиллерии заняли станицу, и казачье население всех 
  возрастов было арестовано. Арестантами были забиты все подвалы в станице. <br>
  <br>
  Однако с приходом Добровольческой армии на Кубань под командованием генерала 
  Л. Г. Корнилова казаки Лабинского отдела окончательно определяют свое место. 
  Верные присяге и долгу, они вливаются в части Добровольческой армии. При штурме 
  Екатеринодара погибает генерал Корнилов и начинается отход Добровольческой армии, 
  которую возглавил генерал А. И. Деникин, к Дону, где воевали донские казаки. 
  Вместе с ней уходит в донские степи Кубанская армия, возглавляемая генералами 
  Покровским и Шкуро. <br>
  <br>
  В мае 1918 года Добровольческая армия выступила во второй "кубанский" поход. 
  Вот тогда-то в станицах Лабинского отдела вновь поднимаются казаки. <br>
  <br>
  В ст. Чамлыкской организатором освободительного движения казачества стал подхорунжий 
  Комаревцев. Ему удалось объединить станичных казаков в боеспособные отряды. 
  Казаки станиц Чамлыкской, Владимирской и Константиновской должны были подступить 
  к Лабинской и при поддержке лабинских казаков овладеть ею. В Лабинской имелись 
  арсеналы, два десятка пулеметов, находившиеся в распоряжении местного гарнизона, 
  и пушки. Далее казаки ст. Вознесенской должны обезоружить местный гарнизон и 
  быстро маршем идти в ст. Чамлыкскую. Сюда должно было подойти подкрепление из 
  ст. Родниковской и, получив сообщение из ст. Лабинской, выступить в направлении 
  к Армавиру. По дороге к ним присоединяются казаки из Упорной. <br>
  <br>
  Организаторы выступления рассчитывали в случае успеха центр боевых действий 
  перенести под Армавир, в случае неудачи - отступить через Владимирскую и Зассовскую 
  в горы. Но этим планам не суждено было осуществиться. <br>
  <br>
  Накануне праздника Святой Троицы чамлыкские казаки были готовы выступить. Но 
  почему-то не подошли казаки из Вознесенской. Не было никаких известий от константиновских 
  казаков. Связавшись с владимирцами, все же решили двинуться к Лабинской. Наступление 
  повели в двух направлениях: чамлыкцы в районе вокзала, владимирцы - с юго-восточной 
  стороны. Казаки ворвались в станицу и успели захватить пушки, но вывезти их 
  было не на чем. Да и помощи со стороны казаков Лабинской не получили. Наступавшим 
  казакам оказалось не под силу справиться с гарнизоном красных численностью до 
  семи тысяч человек. Не смогли они захватить и оружейные склады, расположенные 
  на Тюремной площади (ныне микрорайон "Черемушки"). Отсюда по казакам ударили 
  пулеметы, и они вынуждены были отступать. Налетевшая конница врезалась между 
  чамлыкцами и владимирцами, заставила их отступать в чистое поле за железнодорожным 
  полотном. Бой был жестокий и кровавый. Здесь погиб организатор восстания подхорунжий 
  Комаревцев. С этого момента можно считать, что восстание провалилось. Казаки 
  в места боя уходили небольшими группами и в одиночку, рассчитывая, что вновь 
  соберутся в родной станице, все еще надеялись на помощь вознесенцев и урупцев. 
  Но ни те, ни другие так и не подошли. <br>
  <br>
  А местная власть незамедлительно стала принимать меры. Густые цепи красноармейцев 
  окружили лесок, где укрывались казаки, и переловили почти всех. Многие были 
  расстреляны тут же. Некоторых доставили в станицу, чтобы судить военным трибуналом. 
  Все, кто принимал участие в восстании, были приговорены к смертной казни. Связанных 
  казаков выводили из подвала и на глазах родных и близких рубили шашками. За 
  три дня были казнены 940 человек. Погибли казаки, которые воевали в составе 
  Кавказской армии белых, и о действиях которых с высокой похвалой отзывался генерал 
  П. Н. Врангель. <br>
  <br>
  Впрочем, чамлыкские казаки тоже глубоко уважали своего командира и выбрали его 
  почетным казаком станицы Чамлыкской. Будучи в изгнании в Югославии, но оставаясь 
  Главнокомандующим Русской армии, генерал П. Н. Врангель чувствовал себя, как 
  в родной семье среди своих станичников - казаков ст. Чамлыкской и Константиновской. 
  <br>
  <br>
  Восстание казаков в июне 1918 года, которое получило название Троицкого, имело 
  весьма трагические последствия не только для чамлыкских казаков. Море крови 
  пролилось в ст. Упорной, Лабинской, Зассовской, Владимирской, Вознесенской. 
  Убивали в основном старых казаков, ведь молодые были на фронте. Только в ст. 
  Упорной казнены более ста человек. В Вознесенской во время похорон комиссара 
  А. И. Гурского его братья по оружию прямо на гробах, покрытых кумачом, рубили 
  головы казакам. В день похорон Гурского казнили 90 человек, среди казненных 
  был и священник А. Ивлев. <br>
  <br>
  После неудачного Троицкого восстания 68 лабинских казаков были арестованы и 
  отправлены в тюрьму Пятигорского ЧК. Среди арестованных был есаул Георгий Федоров 
  со своим братом. В первую годовщину октября все 68 человек были зверски изрублены 
  у кладбища. Имена обоих братьев оказались в списках казненных, но они были единственными, 
  кто смог спастись, исчезнув в сумерках и суматохе. Прорвавшись через фронт, 
  они присоединились к Кубанскому войску. <br>
  <br>
  Троицкое восстание стало еще одним свидетельством того, что казачество не могло 
  смириться с тем, что были разрушены вековой уклад жизни, порядок и устои целого 
  народа, не могли согласиться с теми идеями, которые вносили раскол и делили 
  людей на друзей и врагов, не понимали, что означает классовая борьба и зачем 
  она нужна. Они не принимали таких идей, ради которых нужно убивать 900 жителей 
  Чамлыкской, 100 упорненцев, 90 вознесенцев. <br>
  <br>
  После поражения Добровольческой армии в Крыму и отступления Кубанского войска 
  через перевалы Кавказского хребта многие лабинские казаки оказались на чужбине. 
  <br>
  <br>
  В начале ноября 1920 года началась памятная эвакуация казаков из Крыма. Они 
  оставляли оружие (за исключением холодного), своих коней и грузились на пароходы 
  и военные корабли. Некоторое время они провели в Константинополе. В конце ноября 
  1920 года генералом Врангелем было принято решение переместить Донской и Кубанский 
  корпуса на остров Лемнос в Эгейском море. Но часть казаков осталась в Константинополе, 
  часть осела на севере Африки, в Сербии, Польше. <br>
  <br>
  В апреле 1921 года партия казаков отбыла с острова в далекую Бразилию. Пароход 
  "Рион" уносил на другой конец света 177 кубанцев. <br>
