	<strong>Виктор Калугин</strong> <br>
  <br>
  <span class="header"><strong>ДВА ВЕКА РУССКОЙ ПЕСНИ </strong><br><br>
  </span>


  <em>Предисловие к сб.: Антология русской песни. – М.: Изд-во Эксмо, 2005.</em><br>
  <br>
  <br>
  Песня — самый молодой и одновременно самый древний музыкальный и поэтический 
  жанр. Ее народным истокам — тысячи лет, а современной форме — всего лишь двести. 
  Так на рубеже двух веков (XVIII и XIX) стали называть стихотворные стилизации 
  народных песен, которые по сравнению с «простонародными» были уже не просто 
  народными, а поэтическими произведениями в духе своего времени. «Ах! Как ты 
  жестока! — вскричал неведомый Богатырь. Красавица смутилась и робким голосом 
  вопрошала: — Кто ты? Дерзкий! Телесно ли существо? — Я существо, тебя обожающее, 
  немогущее дыхать, чтоб дыхание мое не подкрепляемо было твоею любовью!» — объяснялся 
  Алеша Попович со своей возлюбленной в одной из подобных же стилизаций народных 
  былин. Но, как говорится, лиха беда начало... <br>
  <br>
  Уже в 20—30-е годы XIX века имена родоначальников жанра «русской песни» И. И. 
  Дмитриева, Ю. А. Нелединского-Мелецкого, А. Ф. Мерзлякова будут восприниматься 
  как безвозвратно ушедшее прошлое. Золотой пушкинский век погребет заживо многих 
  своих предшественников, но сами «русские песни» останутся и как поэтический 
  жанр, и как некое знаковое явление ушедшего времени. В пушкинском «Домике в 
  Коломне» говорится о героине: <br>
  <br>
  В ней вкус был образованный. Она <br>
  Читала сочинения Эмина. <br>
  Играть умела на гитаре <br>
  И пела:<em> Стонет сизый голубок</em>, <br>
  И <em>Выду ль я</em>, и то, что уж постаре.<br>
  <br>
  Пушкин называет четыре составные образованного вкуса конца XVIII века: наиболее 
  востребованные авантюрные романы Федора Эмина (он был родоначальником этого 
  жанра), умение играть на гитаре и самые популярные «русские песни» Дмитриева 
  и Нелединского-Мелецкого.<br>
  <br>
  В пушкинские времена таким же знаковым стал <a href="../romans/alabiev/solovej.php">«Соловей»</a> Дельвига — Алябьева. После 
  «Соловья» уже не надо было никому объяснять Qu’est-ce que c’est русская песня, 
  она встала в один ряд с итальянскими, немецкими, французскими. «Соловей» впервые 
  прозвучал в Большом театре 7 января 1827 года в исполнении кумира московских 
  меломанов Петра Булахова (Сергея Лемешева того времени) и вошел в репертуар 
  самых выдающихся русских и зарубежных исполнителей.<br>
  <br>
  Современники Пушкина слышали «Соловья» в исполнении знаменитой Прасковьи Бартеневой, 
  немецким «оперным дивом» Генриэттой Зонтаг (ее даже называли «соловьем»), а 
  в 40—50-е годы «Соловей» стал коронным номером Полины Виардо, включавшей его 
  во все свои концертные программы и в сцену урока пения в «Севильском цирюльнике», 
  что вызывало шквал аплодисментов.<br>
  <br>
  Ближайший лицейский друг Пушкина барон Дельвиг - центральная фигура в судьбах 
  русской песни. По сути, он и является первым поэтом-песенником. Пушкин тоже 
  внес свою лепту в создание песенного жанра, у него есть песни: украинская (<a href="kazak.htm">«Казак»</a>), 
  молдавская («Черная шаль»), татарская (<a href="../romans/tatarp.htm">«Дарует небо человеку»</a> из «Бахчисарайского 
  фонтана»), цыганская (<a href="../romans/starmuz.htm">«Старый муж, грозный муж»</a> из поэмы «Цыганы»), испанская 
  (<a href="../romans/ispanr.htm">«Ночной зефир»</a>), грузинская (<a href="../romans/nepojkras.htm">«Не пой, красавица, при мне»</a>), черкесская («В 
  реке бежит гремучий вал» из «Кавказского пленника»), шотландская (<a href="voronkvor.htm">«Ворон к ворону 
  летит»</a>) и... ни одной русской. Исследователи литературы вынуждены проделывать 
  невероятные кульбиты, чтобы хоть как-то объяснить подобную неувязочку. Но все 
  обстоит, как мне представляется, намного проще. Пушкин попросту уступил свободную, 
  не занятую им самим нишу своему «парнасскому брату». И это был воистину царский 
  подарок! <br>
  <br>
  Самое непосредственное отношение к созданию жанра «русской песни» имел еще один 
  лицейский товарищ Пушкина и Дельвига — Михаил Яковлев. Уже в Лицее он получил 
  известность благодаря своему <em>бархатному</em> голосу, а позднее вошел в число 
  ведущих исполнителей и композиторов-песенников, сохранив верность лицейскому 
  братству и в выборе текстов. Пушкин называл его <em>нашим песенником</em>. Самые 
  известные песни и романсы Михаил Яковлев создал на слова Пушкина и Дельвига. 
  Среди них — <a href="../romans/buriamgloju.htm">«Зимний вечер»</a> («Буря мглою небо кроет... »), впервые прозвучавший 
  в 1832 году почти одновременно с <a href="../romans/zimdoroga.htm">«Зимней дорогой»</a> А. А. Алябьева. В дальнейшем 
  к этому пушкинскому стихотворению обращались многие композиторы (более 25), 
  в том числе А. С. Даргомыжский, но самой известной мелодией осталась яковлевская. 
  Не был чужд Михаил Яковлев и поэзии. В популярной песне <a href="../romans/kogotonet.htm">«Кого-то нет, кого-то 
  жаль»</a> ему принадлежат и музыка, и слова. Даргомыжский создал на его стихи романс 
  <a href="../romans/dargomyzsky/onapridet.htm">«Она придет»</a>. <br>
  <br>
  Пушкин не создавал «русские песни», тем не менее два его самых ранних лицейских 
  стихотворения стали таковыми. Обычно мы говорим о влиянии фольклора на поэзию, 
  о его воплощении в те или иные поэтические формы. Но существует и обратный процесс 
  — фольклоризация литературных произведений. Подобное произошло с «русской песнью» 
  Д. П. Глебова «Скучно, матушка, мне сердцем жить одной», впервые опубликованной 
  в 1817 году и ничем особо не отличавшуюся от подобных песен-стилизаций. Но в 
  1842 году эту песню обработал Алексей Варламов, и его музыкальный вариант <a href="vdolpoulice.htm">«Вдоль 
  по улице метелица метет»</a> до сих пор входит в число самых популярных народных 
  песен. При этом Варламов почти ничего не изменил в первоисточнике, он лишь отсек 
  все «лишнее».<br>
  <br>
  Не менее примечательная метаморфоза произошла с двумя другими «русскими песнями» 
  того времени. В 1818 году в печати появилось стихотворение Александра Дуропа 
  <a href="kazaknatchuz.htm">«Казак на чужбине»</a>, выдержанное в романтическом стиле баллад В. А. Жуковского 
  (лишь Пушкин сможет преодолеть его <em>стихов пленительную сладость</em>). Начиная 
  с 30-х годов это стихотворение стало входить во все основные сборники казачьих 
  и солдатских песен, а затем появились первые записи фольклористов уже не стихотворения, 
  а чисто казачьей песни <a href="../kazaki/priluzku.htm">«На лужке, лужке, лужке»</a>. Но в процессе фольклоризации 
  «Казак на чужбине» не просто перешел из одной формы бытования — письменной, 
  в другую — устную, а перевоплотился. П р о и з о ш л о ч у д о р о ж д е н и 
  я н а р о д н о й п е с н и.<br>
  <br>
  Стихотворение Дмитрия Ознобишина «Чудная бандура» было впервые опубликовано 
  в 1835 году. Мы никогда не узнаем, как и когда оно превратилось в народную казачью 
  песню <a href="podonu.php">«Гуляет по Дону казак молодой»</a> («сия тайна велика еси»), но факт остается 
  фактом: вот уже более полутора веков она является едва ли не самым популярным 
  казачьим шлягером. И воспринимается как подлинно народная. Так оно и есть! Хотя 
  первоисточник — не только литературное произведение, но вдобавок еще и переводное. 
  Дмитрий Ознобишин обработал шведскую народную балладу, в которой, естественно, 
  не было никакой бандуры и никаких казаков. Шведская же баллада, в свою очередь, 
  относится к числу так называемых «бродячих сюжетов», совпадающих в фольклоре 
  всех стран и народов. Правда, еще никому не удалось выявить, как <em>бродят</em> 
  подобные сюжеты по миру. А потому первоисточником казачьей песни мы с полным 
  правом можем считать один из мировых «бродячих сюжетов», который <em>забрел</em> 
  на Тихий Дон, благодаря «Чудной бандуре» Ознобишина. Вот так-то, знай наших! 
  Молодцы донцы!.. <br>
  <br>
  А пушкинские лицейские стихи «Казак» («Раз, полношною порою») и «Романс» (<a href="podvetcher.php">«Под 
  вечер осенью ненастной»</a>) превратились в народные песенные баллады, близкие к 
  «жестоким» романсам начала ХХ века. И в этом <em>превращении</em> есть что-то 
  мистическое, если вспомнить, что «Под вечер осенью ненастной» пели нищие в поездах... 
  <br>
  <br>
  От Дельвига один шаг до Алексея Кольцова. Дельвиг, <em>в гроб сходя, благословил</em> 
  Алексея Кольцова, опубликовав в 1830 году в «Литературной газете» первую песню 
  <em>самодурного поэта</em> из Воронежа. Это было его знаменитое <a href="kolco.htm">«Кольцо»</a>, к 
  которому обращались А. С. Даргомыжский, А. Л. Гурилев, Петр П. Булахов, С. В. 
  Рахманинов. Кольцовские традиции — самые плодотворные в русской песенной поэзии. 
  В XIX веке, наряду с пушкинской плеядой поэтов, можно выделить тютчевскую, некрасовскую, 
  фетовскую и кольцовскую. К кольцовской плеяде, помимо И. С. Никитина и И. 3. 
  Сурикоза, имена которых вошли в число классиков, можно отнести едва ли не всех 
  поэтов-песенников, оставшихся «безымянными», уже при жизни попавших в число 
  «забытых». Вот лишь некоторые из них: Николай Цыганов, Александр Тимофеев, Василий 
  Красов, Семен Стромилов, Иван Макаров, Василий Чуевский. Этот ряд — <em>самых 
  неизвестных авторов самых известных песен</em> — можно продолжить десятками 
  других имен. <br>
  <br>
  Никакие последующие публикации не смогли вернуть их из небытия. В памяти поколений 
  остались их песни, а не имена (чаще все происходит как раз наоборот: запоминаются 
  имена, а не произведения). В этом может убедиться всякий, если назвать песни: 
  <a href="nechej.htm">«Ты не шей мне, матушка, красный сарафан»</a> Николая Цыганова; <a href="tonevetervetku.htm">«То не ветер ветку 
  клонит»</a> Семена Стромилова; <a href="1zvutchno.php">«Однозвучно гремит колокольчик»</a> Ивана Макарова; <a href="pylstolb.htm">«Тройка»</a>  («Пыль столбом крутится, вьется»), <a href="../romans/bulahov/gorigori.php">«Гори, гори, моя звезда»</a> Василия Чуевского... 
  <br>
  <br>
  Объясняется это не столько исторической несправедливостью, сколько законами 
  самого песенного жанра. На принципе анонимности основана устная народная литература 
  всех стран и народов. Любая песня, вне зависимости от происхождения, фольклорного 
  или литературного, по мере роста популярности не умножает славу автора, а сводит 
  ее на нет. Достигнув пика популярности, песни «теряет» имя автора. С чего, собственно, 
  и начинается ее вторая жизнь как народной песни. До этого момента она остается 
  «авторской». <br>
  <br>
  Все это в полной мере испытали на себе и классики, чья слава впрямую никак не 
  связана со славой их песен. Имена Пушкина, Лермонтова, Есенина точно так же 
  отходят на второй план, а часто и вовсе «забываются». В этом залог их народности! 
  <br>
  <br>
  Алексей Кольцов — исключение. Его, наверняка, ждала бы участь всех поэтов-песенников, 
  если бы не «неистовый Виссарион». Белинский был в одинаковой степени <em>неистовым</em> 
  как в уничтожении своих литературных противников, так и в возвеличивании тех, 
  кто не входил в их число. Алексей Кольцов, по чистой случайности, оказался среди 
  вторых. <br>
  <br>
  Композиторы-песенники, как и поэты-песенники, если и занимали в неписаной «Табели 
  о рангах» (а в России неписаные законы всегда сильнее писаных) первые места, 
  то неизменно с конца. В числе <em>второстепенных</em>, к примеру, до сих пор 
  числятся братья Булаховы, хотя в данном случае мы имели дело с целой династией 
  певцов и композиторов-песенников, сыгравших далеко не второстепенную роль в 
  музыкально-поэтической культуре всего XIX века. Об основателе этой династии 
  — Петре Булахове — уже говорилось как о первом исполнителе «Соловья» Дельвига 
  — Алябьева. Но и слава самого первого и самого известного в те времена пушкинского 
  романса «Черная шаль» тоже неотделима от его имени.<br>
  <br>
  С именами его сыновей, Петра Петровича Булахова и Павла Петровича Булахова, 
  связаны, но уже как композиторов, одни из самых популярных песен второй половины 
  XIX века. Петр П. Булахов — автор музыки песен <a href="gremzvonok.htm">«Вот на пути село большое»</a> И. 
  Анордиста, «Вы скажите мне, люди добрые» А. Кольцова, <a href="kolokol.htm">«Колокольчики мои, цветики 
  степные»</a> А. К. Толстого, «Тройка» («Пыль столбом кружится, вьется») В. Чуевского 
  и многих других. Народные песни <a href="../rus/snegibel.htm">«Не белы снеги»</a>, <a href="../bezzem/nekukuch.htm">«Не кукушка во сыром бору куковала»</a>; 
  «Не одна ты во поле, дороженька» до сих пор исполняются в булаховской обработке. 
  Павел П. Булахов, как и отец, был ведущим оперным певцом Императорских театров. 
  На концертной сцене в его исполнении обрели жизнь песни и романсы многих композиторов, 
  в том числе собственные. Он прославился исполнением своих песен: <a href="ahtyvolga.htm">«Ах ты, Волга-матушка»</a>  на стихи В. Чуевского, а также одной из самых знаменитых «Троек» (<a href="3mtchitsa.htm">«Тройка мчится, 
  тройка скачет»</a>) на стихи Петра Вяземского. <br>
  <br>
  Более всего из композиторов-песенников не повезло самому талантливому из них 
  — Алексею Варламову. Утверждая принципы <em>программной</em> музыки, В. В. Стасов 
  (один из <em>неистовых</em>, но не в литературе, а в музыке и изобразительном 
  искусстве) на всех безыдейных композиторов, не укладывавшихся в прокрустово 
  ложе его теории, навешивал ярлык «варламовщина». Пройдет совсем немного времени 
  (чуть более полстолетия) и подобный же идеологический ярлык «неистовые ревнители» 
  Пролеткульта применят к Есенину — <em>варламовщина</em> превратится в <em>есенинщину</em>. 
  <br>
  <br>
  Золотой пушкинский век начинался с «русских песен», тем не менее, при всем желании, 
  его трудно назвать песенным. Это был Золотой век романса, время создания шедевров 
  <a href="../romans/glinka/japomniu.htm">«Я помню чудное мгновенье»</a> Пушкина — Глинки, <a href="../romans/glinka/neiskuchaj.htm">«Не искушай меня без нужды»</a> Баратынского 
  — Глинки, <a href="notchsmotr.htm">«Ночной смотр»</a> Жуковского — Глинки, <a href="vetchzvon.htm">«Вечерний звон»</a> Козлова — Алябьева, 
  <a href="../romans/varlamov/belparus.htm">«Белеет парус одинокой»</a> Лермонтова — Варламова, <a href="../romans/tutchkineb.htm">«Тучки небесные»</a> Лермонтова 
  — Даргомыжского. <br>
  <br>
  Романсной была и вся вторая половина XIX века, соединившая поэзию А. А. Фета, 
  Я. П. Полонского, А. Н. Апухтина, А. К. Толстого с музыкой великих композиторов 
  «могучей кучки» и не менее великого, но ни с кем <em>не кучковавшегося</em> 
  П. И. Чайковского. Романсным был и Серебряный блоковский поэтический век, связанный 
  с именами выдающихся поэтов и выдающихся романсных композиторов ХХ века С. В. 
  Рахманинова и А. Т. Гречанинова.<br>
  <br>
  В ХХ веке все изменилось. Песня взяла реванш — вышла на первое место. ХХ век 
  стал песенным веком. <br>
  <br>
  Подобная «рокировка» произошла, увы, не в силу той неизбежной борьбы литературных 
  направлений и стилей, которой отмечен XIX век. В ХХ веке романсы отошли на второй 
  план совсем по другим причинам, по которым Рахманинов, Гречанинов, Федор Шаляпин, 
  Надежда Плевицкая, Александр Вертинский и почти все ведущие поэты Серебряного 
  века оказались вне России, а сам жанр романса уничтожен, стерт с лица земли. 
  Уцелела лишь песня. Ей суждено было пережить <em>маяковизацию</em>. Песня стала 
  таким же феноменом ХХ века, каким в XIX веке был романс. <br>
  <br>
  Но сейчас уже и песенный ХХ век ушел в прошлое, стал достоянием истории. Настала 
  пора его осмысления... <br>
  <br>
  «Из песни слова не выкинешь!» — гласит пословица. Выкидывали, еще как выкидывали, 
  даже слова Гимна переписали. Что, впрочем, вполне соответствует одному из основных 
  принципов народного творчества. В народной поэзии и в народной прозе нет и не 
  может быть какого-то одного раз и навсегда утвержденного, канонического текста. 
  Неизменяемой, как правило, остается мелодия. На одну и ту же мелодию (среди 
  них тоже немало «бродячих») могут звучать самые разные песни. Мелодия принадлежит 
  к миру <em>горнему</em> — неизменному, вечному. Слова — <em>дольнему</em>. Они 
  изменяются, как и сама жизнь. В этом суть многовариантности устного народного 
  творчества и его принципиальное отличие от зафиксированной, «застывшей» письменной 
  литературы, неразрывно связанной со своим временем. <br>
  <br>
  Пословица имеет два смысла — буквальный и переносный. В буквальном смысле она 
  применима именно к письменной литературе, к авторским произведениям; в переносном 
  — к человеческой жизни, к истории, из которой действительно ничего нельзя <em>выкинуть</em>. 
  В том числе и песни, являющиеся частью этой истории. <br>
  <br>
  Что и дало о себе знать в изменении слов Гимна. Казалось бы, что особенного: 
  изменилась эпоха, почему не могут измениться слова? Изменили и... сквозь новые 
  слова продолжают проступать старые. Все это говорит о том, что Гимн должен остаться 
  достоянием Истории, как это однажды уже произошло с Гимном <a href="boze.php">«Боже, Царя храни!»</a>  <br>
  <br>
  Музыка Гимна, как и музыка сакральной песни <a href="../ww2/oficial/svvojna.php">«Священная война»</a>, созданы Александром 
  Васильевичем Александровым — последним регентом хора храма Христа Спасителя. 
  Храм взорвали, но ведь на его месте так и не смогли ничего построить. Бассейн 
  — не в счет. Он был не чем иным, как готовым котлованом для будущего восстановления 
  храма Христа Спасителя... <br>
  <br>
  В песенной истории ХХ века можно выделить несколько периодов: песни революции 
  и гражданской войны; песни 30-х годов; песни Великой Отечественной войны; «авторские 
  песни» <em>шестидесятников</em>. Наименее известными до сих пор остаются песни 
  гражданской войны. Дело в том, что почти все они (типа <a href="../grvojna/oficial/poschorse.php">«Шел отряд по берегу»</a>, 
  <a href="../grvojna/oficial/pokahovke.php">«Каховка»</a>) созданы в 30-е годы и являются такими же художественным произведениями 
  в жанре «фантазии», как легендарный «Чапаев», имеющий к реальности примерно 
  такое же отношение, как анекдоты о Чапаеве, но в ином жанре — мифологическом. 
  Речь идет не только о проблеме исторической достоверности. В Отечественную войну 
  <a href="../ww2/oficial/solovji.htm">«Соловьи»</a> и <a href="../ww2-germ/lillimarleen.htm">«Лили Марлен»</a> звучали по разные линии фронта, на разных языках; 
  в гражданскую по обе линии фронта пели одни и те же русские песни, на одном 
  и том же русском языке. Но... <br>
