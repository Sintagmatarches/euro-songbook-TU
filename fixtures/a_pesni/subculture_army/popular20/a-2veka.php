  и даже не пятьдесят, а более ста пятидесяти песен на стихи Рубцова созданы десятками 
  композиторов и входят в репертуар десятков исполнителей. <br>
  <br>
  Чудо возникновения песенной поэзии Николая Рубцова произошло на наших глазах, 
  но произошло не в одночасье. Для этого потребовалось ровно четверть века. <br>
