  1942 года. А «Тальяночка», так называли песню, стала одной из самых любимых, 
  звучала на всех фронтах. Более двух лет оставались в рукописи «Соловьи». Фатьянов 
  не решался показать их даже Соловьеву-Седому. Не отдавал и в газеты, не верил, 
  что стихи смогут появиться в печати. Стихотворение так и не было опубликовано, 
  а песня, впервые появившаяся в 1944 году, не знала равных по популярности. Редко 
  кто из фронтовиков, слушая ее, мог сдержать слезы.<br>
  <br>
  В самом конце войны, буквально в мае 1945 года, зазвучал еще один песенный шедевр 
  Фатьянова — Соловьева-Седого «Давно мы дома не были». Но началась мирная жизнь, 
  а с ней вступили в силу и ее законы: гораздо более жестокие, чем во время войны, 
  которая «все спишет»... Не прошло и года после награждения орденом Красной Звезды 
  (Фатьянов наконец-то мог появиться на равных среди писателей-орденоносцев), 
  как грянул гром — Постановление ЦК КПСС 1946 года, в котором рядом с именами 
  Михаила Зощенко и Анны Ахматовой стояли и другие имена. В «черном списке» оказался 
  и Алексей Фатьянов, названный «поэтом кабацкой меланхолии». Не правда ли, знакомая 
  формулировочка? Все оттуда же — из «варламовщины», из «есенинщины» торчат ее 
  «уши»! «Кабацким поэтом» называли Сергея Есенина, в «кабацкие» попал и Алексей 
  Фатьянов. До сих пор ходит эпиграмма: «Встретил я Фатьянова, трезвого — не пьяного. 
  Ежели не пьяного, значит — не Фатьянова». <br>
  <br>
  Такими пытались представить самых выдающихся русских поэтов ХХ века: Есенина, 
  Фатьянова, Рубцова... Но ведь песни их <a href="../drugije/klen.htm">«Клен ты мои опавший, клен заледенелый»</a>, 
  «Соловьи», <a href="../drugije/zimnp.htm">«В этой деревне огни не погашены»</a> тоже стоят рядом. И книги, вышедшие 
  одна за другой в знаменитейшей серии «ЖЗЛ» — о Есенине и (мыслимое ли дело!) 
  о Рубцове, о Фатьянове, тоже составили совершенно новый ряд биографий. Все это 
  — вполне реальные итоги ушедшего ХХ века. <br>
  <br>
  Возвращение идеологического гнета испытал на себе и автор культовой «Катюши» 
  — Исаковский. Не было и до сих пор нет песни, равной «Катюше» по узнаваемости 
  во всем мире. Более ста ее вариантов записано во время войны и в послевоенные 
  годы. Но никто, уверен, так и не смог разгадать, в чем ее «секрет песенности». 
  Не меньшей популярностью пользовались его песни военных лет: <a href="../ww2/oficial/vlesuprifront.php">«В прифронтовом 
  лесу»</a>, «Огонек» («На позиции девушка»), «Враги сожгли родную хату». Интересно 
  отметить, что первая и последняя удивительным образом перекликаются с первой 
  и последней фронтовыми песнями Фатьянова. <br>
  <br>
  Только у Исаковского в <em>прифронтовом лесу</em> «старинный вальс, осенний 
  вальс играет гармонист», а у Фатьянова <em>на солнечной поляночке</em> в таком 
  же прифронтовом лесу, такой же гармонист на тальяночке «играет про любовь». 
  Речь, естественно, идет не о заимствованиях, а о сходстве типологических ситуаций. 
  Обе песни созданы в 1942 году. Не менее характерны как совпадения, так и различия 
  в песнях 1945 года. В фатьяновской песне «Давно мы дома не были» — предвкушение 
  радости возвращения в родной дом, где все «как будто в сказке-нежити» и неизменное 
  в такой ситуации: «Налей, дружок, по чарочке, по нашей фронтовой». В песне Исаковского 
  «Враги сожгли родную хату» — само возвращение на пепелище родного дома и поминальная 
  тризна: «И пил солдат из медной кружки вино с печалью пополам». И оба сюжета, 
  между прочим, тоже принадлежат к числу «бродячих» в мировом фольклоре. <br>
  <br>
  «Враги сожгли родную хату» — самая трагическая песня Отечественной войны, песня-драма 
  эпического масштаба. Что, собственно, и решило ее судьбу в победном 1945 году. 
  Песню запретили. Лишь через пятнадцать лет, в 1960 году, Марк Бернес на свой 
  страх и риск включил ее в свой репертуар, и она получила всенародное признание. 
  <br>
  <br>
  По странной иронии судьбы, запрет на песню Исаковского был снят в те самые годы, 
  когда среди запрещенных оказались десятки так называемых «авторских песен» поэтов-песенников 
  нового поколения «шестидесятников». В последние два десятилетия ХХ века этот 
  песенный феномен был достаточно хорошо изучен и представлен в поэтических и 
  песенных изданиях. Отмечу лишь, что «авторские песни», при всей своей феноменальной 
  популярности, так и остались авторскими, то есть не перешли того рубежа, когда 
  песни «теряют» имя автора. Наоборот. Невероятно усилилось именно авторское начало. 
  <br>
  <br>
  Они были и остаются именно песнями Булата Окуджавы, Галича, Визбора, Новеллы 
  Матвеевой, неотделимые от личности, манеры исполнения, интонации голоса, от 
  соединения в одном лице поэта, композитора и исполнителя. Немалую роль сыграл 
  и социальный фактор: запретный плод потому и сладок, что запретный. Исчезли 
  запреты — исчезла и сладость... <br>
  <br>
  Песенный ХХ век завершился несомненным триумфом «авторской песни», которая наверстала 
  все упущенное в предшествующее столетие. С этим фактом придется считаться любому 
  исследователю. Равно как и с другими. Судьбы песен — самые непредсказуемые и 
  причудливые! Ну, скажите на милость, кто мог представить в начале 90-х годов 
  — во времена крушения советской империи, что песням ее предстоит второе рождение? 
  Что вновь зазвучат и вновь побьют все рекорды по популярности ретро-шлягеры 
  из фильма «Свинарка и пастух», выброшенные, как казалось многим, на свалку истории, 
  утилизованные? «Старые песни о главном» вернулись на телеэкраны, стали составной 
  частью едва ли не всех музыкальных теле-шоу типа «Угадай мелодию», новогодних 
  телепрограмм, «Караоки», вошли в репертуар ведущих эстрадных звезд. <br>
  <br>
  Это тоже один из «секретов песенности». Как, каким образом запоминаются (или 
  не запоминаются) песенные слова и мелодии? А если уж запоминаются, остаются 
  <em>на слуху</em>, то навсегда входят в генетическую память не одного человека, 
  а всего народа. Как это и произошло с песнями 30-х годов, песнями Отечественной 
  войны, послевоенной песенной лирикой.<br>
  <br>
  Еще один «секрет», но уже не песенности, а ее антипода, связан, как это ни парадоксально, 
  с шоу-бизнесом. Казалось бы, вся эта колоссальная песенная индустрия должна 
  быть основана на запоминаемости песен. А они, хоть ты тресни, не запоминаются 
  или же запоминаются на короткий срок жизни самого сезонного «хита». Проходит 
  сезон, исчезают и «хиты», будто и не было. <br>
  <br>
  Мы вполне можем подвести черту под песенным ХХ веком, под тем, что было и что 
  мы имеем «в остатке». Гораздо труднее представить, каким будет песенный XXI 
  век, будет ли он вообще п е с е н н ы м или же произойдут новые «великие потрясения»? 
  <br>
  <br>
  В конце ХХ века четко обозначилась тенденция, которая, как мне кажется, будет 
  определяющей в XXI веке. Река всегда возвращается в старое русло. Возвратилась 
  и песня — к поэзии. Об этом можно судить по тому, как в последнюю четверть ХХ 
  века, вслед за первыми песнями на стихи Марины Цветаевой, Бориса Пастернака 
  в фильме «Ирония судьбы, или С легким паром» (1975), появились альбомы, песенные 
  циклы на стихи Николая Гумилева, Осипа Мандельштама, Анны Ахматовой и других 
  поэтов Серебряного века, и по тому, как в этот же самый период складывалась 
  судьба песен на стихи Николая Рубцова.<br>
  <br>
  В конце 70-х годов к созданию вокальной поэмы «Золотой сон» на стихи Николая 
  Рубцова приступил Георгий Свиридов, автор самых выдающихся вокальных циклов 
  ХХ века на стихи Александра Блока и Сергея Есенина. Третьим в этом ряду, по 
  замыслу композитора, должен был стать Николай Рубцов. Великий композитор запишет 
  в дневнике: «Весьма возможно, что истинную ценность будут иметь те творцы (т. 
  е. их сочинения), которые как-то отвергнуты средой, но не по признаку «левизны», 
  а по какому-то иному. Например, Николай Рубцов. Это совершенно не случайное 
  явление нашей жизни, не случайная биография и судьба». Ко времени работы над 
  поэмой «Золотой сон» относится и другая дневниковая запись Свиридова: «Николай 
  Рубцов — <em>тихий голос великого народа, потаенный, глубокий, скрытый</em>». 
  Этот <em>тихий голос</em> нам и предстояло услышать в «Золотом сне», состоявшем 
  из пяти стихотворений: «Огороды русские», «Тот да тот от кустика до кустика», 
  «Пасха под синим небом», «Чудный месяц», «Я буду скакать по холмам задремавшей 
  отчизны». Последнее стихотворение Георгий Свиридов выделил, приступив в 1980 
  году к созданию вокального произведения для голоса в сопровождении симфонического 
  оркестра — «Ночной всадник». <br>
  <br>
  Песенная поэма осталась среди незавершенных произведений великого композитора... 
  <br>
  <br>
  В эти же годы зазвучали песни «Тихая моя Родина», «Зимний вечер» Александра 
  Лобзова, которые и ныне остаются одними из лучших в репертуаре Татьяны Петровой. 
  В радиоэфире на долгие годы утвердился шлягер Александра Барыкина «Букет» («Я 
  буду долго гнать велосипед»). В середине 80-х появились первые рубцовские песни 
  Александра Морозова, и среди них — <a href="../drugije/vgornice.htm">«В горнице моей светло»</a>, <a href="../drugije/vminutymuz.htm">«В минуты музыки 
