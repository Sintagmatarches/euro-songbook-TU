  Слова Дмитрия Веневитинова<br>
  <br>
  Под небом Аттики богатой<br>
  Цвела счастливая семья.<br>
  Как мой отец, простой оратай,<br>
  За плугом пел свободу я.<br>
  Но турков злые ополченья <br>
  На наши хлынули владенья…<br>
  Погибла мать, отец убит,<br>
  Со мной спаслась сестра младая,<br>
  Я с нею скрылся, повторяя:<br>
  «За всё мой меч вам отомстит!»<br>
  <br>
  Не лил я слез в жестоком горе,<br>
  Но грудь стеснило и свело;<br>
  Наш легкий челн помчал нас в море,<br>
  Пылало бедное село,<br>
  И дым столбом чернел над валом.<br>
  Сестра рыдала – покрывалом<br>
  Печальный взор полузакрыт;<br>
  Но, слыша тихое моленье,<br>
  Я припевал ей в утешенье:<br>
  «За всё мой меч им отомстит!»<br>
  <br>
  Плывем – и при луне сребристой<br>
  Мы видим крепость над скалой.<br>
  Вверху, как тень, на башне мшистой<br>
  Шагал турецкий часовой;<br>
  Чалма склонилася к пищали,<br>
  Внезапно волны засверкали,<br>
  И вот – в руках моих лежит<br>
  Без жизни дева молодая.<br>
  Я обнял тело, повторяя:<br>
  «За всё мой меч вам отомстит!»<br>
  <br>
  Восток румянился зарею, <br>
  Пристала к берегу ладья,<br>
  И над шумящую волною<br>
  Сестре могилу вырыл я.<br>
  Не мрамор с надписью унылой<br>
  Скрывает тело девы милой –<br>
  Нет, под скалою труп зарыт;<br>
  Но на скале сей неизменной<br>
  Я начертал обет священный:<br>
  «За всё вам меч мой отомстит!»<br>
  <br>
  С тех пор меня магометане<br>
  Узнали в стычке боевой,<br>
  С тех пор как часто в шуме брани<br>
  Обет я повторяю свой!<br>
  Отчизны гибель, смерть прекрасной,<br>
  Всё, всё припомню в час ужасный;<br>
  И всякий раз, как меч блестит<br>
  И падает глава с чалмою,<br>
  Я говорю с улыбкой злою:<br>
  «За всё мой меч вам отомстит!»<br>
