  борьбы греков за независимость была  распространена в русской (и, вообще, 
  в европейской) литературе тех лет (например, &quot;Выстрел&quot; Пушкина, где 
  главный герой Сильвио гибнет в Греции, и др.).<br>
  <br>
  Баллада послужила основой для народных песен и перерабатывалась в народной среде 
  вплоть до эпохи Гражданской и Второй Мировой войн (см. <a href="../grvojna/kr/otecmojbyl.php">&quot;Отец 
