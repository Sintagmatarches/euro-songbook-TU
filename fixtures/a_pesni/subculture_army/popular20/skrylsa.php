<h1>СКРЫЛСЯ, СКРЫЛСЯ МОЙ ГОЛУБЧИК... </h1> 
  <br>
  Скрылся, скрылся мой голубчик,<br>
Из виду совсем пропал.<br>
Я немало слёз роняла<br>
По тебе, голубчик мой.<br>
<br>
Во слезах тебя просила,<br>
Хоть годочек поживём.<br>
- Нельзя, Сашенька милая,<br>
Нельзя часу часовать.<br>
<br>
Час и времечко проходит,<br>
Я к любимой полечу.<br>
Стану на колени,<br>
И прощенья попрошу.<br>
<br>
Стану на колени<br>
И прощенья попрошу.<br>
- Ты прости, прости, милая,<br>
Чем я винен пред тобой.<br>
<br>
- Я за всё, за всё прощаю,<br>
За измену не прощу.<br>
И на узенькой дорожке<br> 
Из нагана застрелю.
<br>
