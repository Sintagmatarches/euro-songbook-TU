По словам Жанны Бичевской, эта песня была записана ею от пожилой женщины в Московской 
области и включена в репертуар, а затем подхвачена другими исполнителями. Может быть, потому 
обычно и встречается один и тот же вариант текста - тот, что поет Бичевская. 
<br>
<br>
В песеннике 1896 года (Новый песенник. Безумная. / Сост. М. Ожегов. Киев: Издание книгопродавца Т. А. Губанова, 1896) автором песни указан М. Ожегов с пометкой &quot;из народных мотивов&quot;, в начале есть еще три куплета, а сама песня озаглавлена &quot;Безумная&quot;. В других песенниках тот же текст приводится без подписи.<br>
<br>
 Встречается <a href="bezumnaja.php">еще одна песня с таким названием</a>, ее авторство приписывается Н. И. Красовскому. В песеннике &quot;Златые горы: новый сборник песен и романсов&quot; 1906 года у песни &quot;Безумная&quot; с подписью Красовского есть эпиграф – четыре строки из текста Ожегова. С эпиграфом из Горького приведена его же песня &quot;Солнце всходит и заходит&quot;. Видимо, Красовский писал свои песни на основе уже известных.

