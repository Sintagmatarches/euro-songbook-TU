ЦЕРКВИ СТОЯЛА КАРЕТА</strong> <br></span>
<br>
У церкви стояла карета,<br>
Там пышная свадьба была.<br>
Все гости нарядно одеты,<br>
Невеста всех краше была.<br>
<br>
На ней было белое платье,<br>
Венок был приколот из роз.<br>
Она на святое распятье<br>
Смотрела сквозь радугу слез.<br>
<br>
Горели венчальные свечи,<br>
Невеста стояла бледна.<br>
Священнику клятвенной речи<br>
Сказать не хотела она.<br>
<br>
Когда ей священник на палец<br>
Одел золотое кольцо.<br>
Из глаз ее горькие слезы<br>
Ручьем потекли на лицо.<br>
<br>
Я слышал, в толпе говорили:<br>
- Жених неприглядный такой.<br>
Напрасно девицу сгубили! –<br>
И вышел я вслед за толпой.<br>
