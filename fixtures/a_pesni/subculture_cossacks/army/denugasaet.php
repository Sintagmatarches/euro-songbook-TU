	<h1>ДЕНЬ УГАСАЕТ 
РУМЯНЫМ ЗАКАТОМ</h1>
	<br>
День угасает румяным 
  закатом, <br>
  Пышный румяный закат, <br>
  А у сестры умирает на груди <br>
  Юный бедняжка — солдат. <br>
  В фартуке белом, обрызганном кровью, <br>
  Тихо сестра подошла. <br>
  Только лишь час, <br>
  Как осколком шкваркнули, <br>
  И в лазарет на кровавой шинели <br>
  С поля его принесли. <br>
  В нем же узнала родимого брата <br>
  И вдруг зарыдала она: <br>
  — Братец мой милый, что же с тобою? <br>
  - Ну, как там родимая мать? <br>
  Мама не слышит, а сердце ее чует, <br>
  Что брат уж готов умирать. <br>
  А там, в темном подвале, в каморке убогой <br>
  Холодно, сыро, темно. <br>
  Мать на кровати лежит разметавшись, <br>
  Видно, больна. <br>
  Полуголодные, бедные детки, <br>
  Руки от холода жмут. <br>
  — Мамочка, мама, открой свои глазки, <br>
  Мама, взгляни хоть на нас. <br>
  Мама не слышит их песен унылых, <br>
  Мама мертва лежит. <br>
  Бедные детки остались сиротками. <br>
  Кто же поплачет о них? <br>
  <br>
