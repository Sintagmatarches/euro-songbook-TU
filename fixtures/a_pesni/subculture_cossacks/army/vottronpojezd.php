  <span class='i'>Две последние строки повторяются<br>
  <br>
   С фонограммы Жанны Бичевской, альбом «Старые русские народные 
  деревенские и городские песни и баллады», Часть 3, Moroz Records, 1998<br>
  </span> <br>
  <br>
  &quot;Неблатной&quot; вариант песни <a href="../dvor/postojparovoz.php">«Постой, 
  паровоз!»</a>. Герой едет навестить маму перед отправкой в армию.<br>
  <br>
  Время появления песни не установлено. Она не фиксируется в дореволюционных сборниках.  
  Есть
  мнение, что эту версию сочинила сама Жанна Бичевская, &quot;состарив&quot; блатной &quot;Паровоз&quot;, предположительно созданный в 1930-е гг. 
  (так говорит, например, исполнитель Андрей Рублевич в интервью для альманаха 
  <a href="../dvor/a-rublevitch.php">&quot;Русский Париж&quot; № 2 (74), 2002 
  г.</a>). 
  С другой стороны, исследователь 
  блатного фольклора Александр Сидоров (Фима Жиганец)  считает эту песню 
  первоисточником &quot;Паровоза&quot;, а не наоборот (Русский шансон: тексты, 
  ноты, история / Сост. и комм. Фимы Жиганца. Напис. нот. текста О. А. Губенко. </span>Ростов-на-Дону, 2005. С. 80-81; статья <a href="../dvor/rostov/a-parovoz.php">&quot;Паровозный 
  детектив: хроника битвы&quot;</a>, 2006). <br>
  <br>
  Размер песни - тактовик с чередованием четырех- и трехударных строк, расшатанный из амфибрахия (Ам4ц3жм). Такая ритмическая схема (четырехударная строка с цезурой плюс трехударная строка) в песнях была популярна по меньшей мере с русско-японской войны - например,  <a href="../rus-jap/umerbedn.php">"Умер бедняга"</a> (Д4ц3жм) и ее блатные переработки. Значит, песня теоретически могла быть написана до 1917 г., хотя это маловероятно ввиду отсутствия ее фиксаций фольклористами. Печальные песни царской армии не преследовались в СССР, их собирали и публиковали открыто, и отсутствие в фольклорных записях этой песни работает не в пользу ее аутентичности.<br>
