        В настоящее время войско собирает сведения по борьбе казаков Оренбургского 
        казачьего войска с большевиками. <br>
        <br>
        В виду того, что означенные сведения, крайне необходимы для включения 
        их в материалы по составлению истории войска, которые уже имеются в военном 
        отделе, до 1854 года включительно и, впоследствии, в историю, — прошу 
        доставить самые подробные сведения по борьбе казаков с большевицкой властью, 
        причем сведения эти должны быть строго разграничены: сведения о боевых 
        подвигах казаков и сведения о тех казаках, кои переходили на сторону большевиков; 
        каждый подвиг офицеров и казаков должен быть оттенен весьма рельефно, 
        равно как и поступки и действия перешедших на сторону советской власти 
        казаков. <br>
