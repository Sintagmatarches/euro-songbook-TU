        </strong><br>
        Большевистский переворот в станице Кичигинской и борьба с ними <br>
        <br>
        Хотя большевистский переворот в столицах и других местах прошел еще в 
        октябре месяце 1917 года, но до нас он докатился только в ночь на Рождество 
        Христово, то есть на 25 декабря 1917 г. <br>
        <br>
        Этим переворотом местное население не было взволновано и относилось ко 
        всему спокойно, так как на местах жизнь шла все прежним путем и казаки, 
        вероятно, думали, что они сохранят свою самобытность и самоуправление, 
        а потому и не волновалось (так в тексте — А.Г.). <br>
        <br>
        Кроме того, о надвигающейся грозе и положении вещей станицы заранее не. 
        были уведомлены. Все готовились спокойно встречать Рождество, вдруг часов 
        в 11 ночи раздается набат, все высыпают на улицу; осматриваются, предполагая, 
        что где-нибудь случился пожар, и, видя, что такового нет, бежали к станичному 
        правлению; где узнали, что на город <u>Троицк</u><sup>16</sup> по железной 
        дороге движутся большевики и что ими уже занята станция <u>Еманжелинская</u>.<br>
        <br>
        О движении поездов вскоре стали давать [знать] и тревожные свистки паровозов, 
        которые шли медленно, исправляя железнодорожный путь, разобранный местами 
        одной из сотен 6<sup><u>го</u></sup> полка. Поднялось страшное волнение: 
        все кругом галдели и не знали, что делать. Часть общественников, у кого 
        только нашлось какое-либо оружие, решила поехать обстрелять железную дорогу, 
        таковых набралось человек 12-15 и во главе с бывшим атаманом станицы П.И. 
        <u>Созоновым</u> поехали к полотну железной дороги. <br>
        <br>
        Началась перестрелка, после которой они вернулись домой и сообщили, что 
        станция Нижнеувельская занята большевиками, и что последние двинулись 
        на Троицк. Население горячилось, почему Окружное Правление не прислало 
        заранее вооружение в станицы и не призвало встать против большевиков. 
        К утру было привезено в станицу 89 винтовок, которые доставил прапорщик 
        <u>Лебедев</u> <u>Яков</u> Иванович, причем сообщил, что если винтовки 
        еще нужны, то будут доставлены, и днем 25 декабря было доставлено еще 
        около 150 винтовок, причем первые 89 винтовок были уступлены делегатам 
        Соколовского поселка Еткульской станицы.<sup>17</sup> Прапорщик же Я.И. 
        <u>Лебедев</u> сообщил, что все оружие, какое только находилось в Троицке, 
        вывезено из него по станицам, и что Окружная Управа эвакуировалась в станицу 
        Кособродскую. <br>
        <br>
        О существовании Окружной Управы ничего больше не получалось (так в тексте 
        — А.Г.). Был собран общественный сход, на котором решили избрать делегатов 
        и послать в Троицк узнать положение вещей. Избрали комитет по самообороне, 
        в состав которого вошли: Отставной Войсковой <u>Старшина Константин</u> 
        Михайлович <u>Стариков</u>, вахмистр <u>Андрей Иларионович Стариков</u>, 
        <u>Федор Мих[айлович] Попов</u>, <u>Яков Федор[ович] Хомутинин</u>, <u>Михаил 
        Ив[анович] Попов</u>, <u>Петр Мих[айлович] Печеркин</u>. Была сформирована 
        дружина самообороны, в состав которой вошли все от 20-летнего до 54-летнего 
        возраста включительно. Эта дружина должна была нести патрульную службу. 
        Она разделялась на 16 взводов, каждому взводу представлялось избрать своего 
        взводного. Ежедневно дежурил один взвод. <br>
        <br>
        В Январе месяце на станицу был получен приказ Малого Войскового Круга, 
        который объявлял большевикам войну. Решено было дружину по самообороне 
        разбить на 4<sup><u>е</u></sup> боевые дружины по 105 человек каждая.<sup>18</sup><br>
        <br>
        Все распоряжения дружины должны были получать от Комитета по самообороне.<br>
        <br>
        19<sup><u>го</u></sup> Января 1918 года с/ст.<sup>19</sup> в нашу станицу 
        прибыл офицерский партизанский отряд численностью в 60 человек во главе 
        с Войсковым Старшиной Мамаевым<sup>20</sup> для выступления против большевиков 
        и нападения на железную дорогу, чтобы отрезать большевикам путь из Челябинска 
        к Троицку.<sup>21</sup><br>
        <br>
        На общем собрании было решено боевым дружинам также выступить против большевиков, 
        но наступление на этот раз не состоялось: решили спросить Окружной Съезд, 
        который в это время заседал в станице Кособродской<sup>22</sup>, последний 
        не разрешил и наступление было отклонено.<sup>23</sup> Вместе с офицерским 
        партизанским отрядом в станицу Кичигинскую приходил отряд <u>Кособродцев</u> 
        человек в 200 под командой сотника <u>Чеурина</u>. <br>
        <br>
        После прихода этих отрядов станица уже была поставлена на ноги; решено 
        было держать тесную связь со всеми станицами. Ежедневно куда-нибудь посылались 
        делегаты в соседние станицы и поселки узнать, как последние ко всему относятся. 
        <br>
        <br>
        Приезжали делегаты и к нам, и мы, в свою очередь, делились своими настроениями. 
        <br>
        <br>
        От партизанского отряда у нас в станице остался <u>Хорунжий Николай Иванович 
        Плотников</u>, который деятельно возбуждал население, чтобы при удобном 
        случае выступить против большевиков; при этом главную задачу (так в тексте 
        — А.Г.) на нашу станицу возлагал порчу, как телеграфа, так и железнодорожного 
        пути.<sup>24</sup> Так как станица находится всего в 3-4 верстах от железнодорожного 
        полотна, вооружения по населению было недостаточно, чтобы послать куда-нибудь 
        отряд, а рядом, кроме того, находилась контрреволюционная<sup>25</sup> 
        Николаевка.<sup>26</sup><br>
        <br>
        В марте месяце числа около 7<sup><u>го</u></sup>,<sup>27</sup> когда из 
        Троицка красноармейцы выступили на Верхнеуральск, где находилось Войсковое 
        Правительство, и в Троицке остался малочисленный красноармейский гарнизон, 
        к нам в станицу приехали Клястицкие делегаты и сообщили, что Клястицы 
        решили напасть на город и что 60 человек партизан послали в ст. Кособродскую 
        для соединения с последними, которые бы отрезали путь отступления красноармейцам, 
        ушедшим на Верхнеуральск. <br>
        <br>
        Решено было также отправить отряд в 65 человек в Кособродскую,<sup>28</sup> 
        а остальных вооруженных оставить в станице для охраны последней со стороны 
        Николаевки и железной дороги, которую решено было разрушить: ждали только 
        каких-либо известий из отряда. В это время получили приказ Войскового 
        Атамана<sup>29</sup> разрушить железную дорогу, который доставил <u>прапорщик</u> 
        <u>Попов</u>. Этот приказ был доставлен вечером 13 марта 1918 года с/ст.<sup>30</sup><br>
        <br>
        В эту же ночь решено было дождаться, когда пройдет пассажирский поезд, 
        а затем занять станцию Нижнеувельскую и начать порчу железной дороги. 
        На станцию был послан вестовой, который бы сообщил, охраняется ли станция, 
        и дождался, когда пройдет поезд. С вечера было отправлено две подводы 
        с людьми и прапорщик Попов за станцию Еманжелинскую, чтобы одновременно 
        испортить хотя телеграф. <br>
        <br>
        В 3<sup><u>м</u></sup> часу утра, когда прошел поезд, партия человек в 
        70 отправилась на станцию Нижнеувельскую, которую заняла и принялась за 
        порчу пути, этими работами руководил <u>Хорунжий</u> <u>Плотников</u>, 
        а охраной прапорщик <u>Николай Андреевич Стариков</u>.<sup>31</sup> К 
        вечеру 14 марта<sup>32</sup> показался разведывательный поезд, который 
        завязал перестрелку с отрядом, разрушавшим железнодорожный путь и потом 
        ушел обратно.<sup>33</sup> Работы продолжались и весь второй день. Показывался 
        ремонтный поезд, исправил один пролет и сожженный деревянный мост и ушел. 
        Мост ночью снова был сожжен. 16 Марта<sup>34</sup> рано утром показался 
        вновь поезд, который стал исправлять путь против хутора Уланова. Казаки 
        этого хутора задумали помешать работам [и] начали перестрелку в числе 
        6 человек. У них было достаточное количество патронов. Красноармейцы открыли 
        по ним пулеметный огонь, затем, образовав цепь, пошли в наступление. <br>
        <br>
        В этой схватке геройски погибли три брата урядники <u>Василий, Григорий 
        и казак Александр</u> Павловичи <u>Улановы</u>.<sup>35</sup><br>
        <br>
        В станице Кичигинской Войсковым Правительством был назначен 3<sup><u>й</u></sup> 
        боевой пункт, куда должны были стянуться силы. Командиром этих частей 
        был назначен Есаул<sup>36</sup> <u>Стариков</u><sup>37</sup>, сюда же 
        прибыл и член Войскового Правительства В.Г. <u>Рудаков</u>.<sup>38</sup> 
        Прибыли дружины Коельская и Таяндинская.<sup>39</sup> Сообщение о том, 
        что большевиками в перестрелке убиты три брата, произвели (так в тексте 
        — А.Г.) удручающее впечатление на всех, а также и на пришедшие дружины, 
        кроме того, с Троицкого фронта прискакал казак <u>Федот Семенов Шумаков</u> 
        и сообщил, что дело проиграно: казаки разъехались с фронта. К вечеру это 
        подтвердилось, казаки приезжали домой. Один из них, Федор Степанов <u>Корелин</u>, 
        в бою с большевиками был убит. <br>
        <br>
        Дружины Коельская и Таяндинская возвратились домой. Это было 16 марта 
        1918 г. с/с.<sup>40</sup><br>
        <br>
        Вечером состоялось собрание, на котором решили подчиниться большевикам. 
        На этом собрании резко выступил урядник 12 Оренбургского казачьего полка 
        Федор Евгеньев Шлыков; за то, чтобы арестовать всех офицеров, находящихся 
        в станице и особенно члена Войскового Правительства Рудакова, его поддерживали: 
        казаки Дмитрий Аверьянов Уланов, Петр Абрамов Бабкин, Тимофей и Федор 
        Ив[ановичи] Созоновы и Михаил Ив. Бабкин, но собрание им категорически 
        отказало, заявив, если нужно, то сами и арестовывайте.<sup>41</sup><br>
        <br>
        Утром 17 Марта<sup>42</sup> сообщили, что на ст. Кичигинскую наступают 
        4 эшалона (так в тексте — А.Г.) красноармейцев и что вместе с тем вооружают 
        село Николаевку. Была избрана делегация с мирными переговорами и послана 
        в <u>ставку Блюхера</u><sup>43</sup>, который и наступал; в делегацию 
        был выбран <u>Федор Ев</u>[геньев] <u>Шлыков</u> и <u>Петр Ив</u>[анов] 
        <u>Созонов</u>. <br>
        <br>
        Часов в 12 эта делегация пешком, с белым флагом отправилась к большевикам, 
        которые верстах в 3 от станицы исправляли путь. Вскоре делегат П.И. <u>Созонов</u> 
        вернулся и передал условия, предъявленные большевиками, а делегат <u>Шлыков</u> 
        все время оставался у большевиков до вечера, вероятно, он же дал список 
        лиц, принимавших деятельное участие в борьбе с большевиками. Так как аресты 
        комиссар <u>Ильиных</u> производил не по опросу, а по списку. Этот же 
        Шлыков был назначен комиссаром в станице. <br>
        <br>
        Комиссар <u>Ильиных</u> приехал в станицу под вечер, предъявил ультиматум 
        в один час сдать оружие, все инструменты, телефоны и телеграфную проволоку, 
        увезенные с железной дороги; уплатить, как будто за порчу железной дороги, 
        контрибуцию в 75000 рублей, исправить телеграф и железнодорожный путь, 
        доставить 200 пудов печеного хлеба, <s>600 пудов пшеницы</s> и <s>200</s> 
        50 пудов мяса.<sup>44</sup> Станица была оцеплена красноармейцами и через 
        час занята. Все требования комиссара были выполнены, а контрибуции, по 
        словам некоторых, было собрано 103000 руб., а по показанным источникам, 
        - 94000 р. <br>
        <br>
        Арестовано было 20 человек местных жителей и два офицера Кожевников и 
        Токарев, а также был задержан на хуторе Нехорошков <u>Хорунжий Николай 
        Ив</u>[анов]<u> Плотников</u> крестьянином Мих[аилом] <u>Лушновым</u> 
        и казаком <u>Иваном Ивановым Угрюмовым</u> и последними вместе с взрывчатыми 
        веществами доставлен в ставку. Все офицеры были расстреляны. С этого времени 
        до прихода чехословаков борьба с большевиками в станице была прекращена. 
        Но как только показались чешские и казачьи части, она в первый же день 
        решила встать против большевиков: несколько человек добровольцев уехали 
        с первыми частями, из каковых я знаю Андрея Петрова Созонова, Вениамина 
        Ив[анова] Бабкина, хотя положение станицы было критическое.<sup>45</sup> 
        Когда через станицу прошел казачий полк и на сходе вынесена резолюция 
        встать в ряды народной армии,<sup>46</sup> тогда со стороны Николаевки 
        станицу обложили красноармейцы, но на этот раз насилий в станице никаких 
        не произвели,<sup>47</sup> арестовали А.К. <u>Лебедкова</u> с сыном Николаем, 
        но последние, благодаря каким-то случайностям освободились. <br>
        <br>
        С тех пор Кичигина вновь не перестает помогать в борьбе с большевиками, 
        как людьми, так и материально, смотря по своим силам. Дай, Бог, эту борьбу 
        нам выдержать до конца! <br>
