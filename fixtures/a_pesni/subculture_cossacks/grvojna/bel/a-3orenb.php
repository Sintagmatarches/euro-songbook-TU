        <span class="header"><strong>БОРЬБА С БОЛЬШЕВИКАМИ НА ТЕРРИТОРИИ 
        III ВОЕННОГО ОКРУГА ОРЕНБУРГСКОГО КАЗАЧЬЕГО ВОЙСКА 1917-1918 гг.: ПО МАТЕРИАЛАМ 
        СТАНИЧНЫХ АНКЕТ </strong></span><br>
        <br>
        <span class="text">Вступительная статья, подготовка текста и комментарии А.В. Ганина<br>
        <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., 2005, «Посев», стр. 185-192</em>.<br>
        <br>
        <br>
        Изучение истории антибольшевицкого движения в Оренбургском казачьем войске 
        и сбор документов по этой теме были начаты еще во время Гражданской войны 
        по инициативе Войскового правительства Оренбургского казачьего войска. 
        <br>
        <br>
        19 сентября 1918 г. Военный отдел Войскового правительства издал за подписью 
        начальника отдела полковника В.Н. Половникова специальное Отношение (Документ 
        1) по вопросу о сборе документов Гражданской войны. Целью сбора документов 
        было включение их в материалы по составлению истории войска. Особое внимание 
        было обращено на описание подвигов казаков, а также на сведения о тех, 
        кто перешел на сторону большевиков. Отношение было выслано атаманам военных 
        округов, командирам всех казачьих частей, в штаб Оренбургского военного 
        округа и в штаб Уральского корпуса. <br>
        <br>
        Ответы на это Отношение были неоднородны. В одних случаях это были изобилующие 
        деталями, развернутые описания событий 1917-1918 гг. (Документы 3-5), 
        в других — лишь краткие отписки (Документ 2), за которыми мог скрываться 
        страх перед большевиками, к которым в будущем могли попасть документы 
        (как, в конечном итоге, и случилось), желание утаить непосредственное 
        сотрудничество с красными или просто не выносить сор из станичной избы. 
        Все прекрасно понимали, что речь шла о недавних событиях, и последствия 
        от написания этих документов могли быть различными. <br>
        <br>
        Собранный «исторический» материал, судя по всему, напрямую повлиял на 
        жизнь некоторых живых людей. Возможно, что именно присланные анкетные 
        данные стали причиной репрессий по отношению к некоторым упомянутым в 
        документах казакам, поддержавшим на начальном этапе войны красных (карательные 
        меры были сравнительно мягкими — лишение казачьего звания, земельного 
        пая и выселение с территории войска). <br>
        <br>
        При публикации документов были выбраны анкеты тех станиц, население которых 
        занимало по отношению к большевикам различные позиции — от нейтралитета 
        и даже одобрения (Документы 2-3) до бескомпромиссной вооруженной борьбы 
        (Документы 4-5). <br>
        <br>
        Бесхитростные описания со всей очевидностью демонстрируют наличие глубокого, 
        непреодолимого раскола в казачьей среде между стариками и вернувшейся 
        с фронта «молодежью». Корни этого конфликта не только в революционных 
        событиях 1917 г. и в Первой мировой войне, но и раньше, — в казачестве 
        конца XIX — начала ХХ вв. <br>
        <br>
        К сожалению, в собрании Российского государственного военного архива сохранились 
        анкеты лишь из III (Троицкого) и, отчасти, II (Верхнеуральского) военных 
        округов, присланные в штабы этих округов из станиц, а также из некоторых 
        воинских частей. Но даже то, что дошло до нас, имеет огромную ценность 
        для историков. В этих документах как бы изнутри, самими казаками, показан 
        механизм возникновения Гражданской войны на территории войска, особенности 
        ведения боевых действий на начальном этапе противостояния. <br>
        <br>
        Масса деталей, имен, ярких описаний позволяет читателю погрузиться в атмосферу 
        описываемой эпохи. Обращает на себя внимание необычное написание казаками 
        новых для них политических терминов: комиссар, большевизм и т.п. <br>
        <br>
        Станичные анкеты могут быть обработаны и проанализированы с применением 
        количественных методов. Имеет смысл также сравнение зарождения социального 
        антагонизма в Оренбургском казачьем войске с аналогичными процессами в 
        других войсках, что позволит в дальнейшем приблизиться к пониманию комплекса 
        проблем начального этапа Гражданской войны и участия в нем казачества. 
        <br>
        <br>
        Документы публикуются в соответствии с современными правилами орфографии 
        и пунктуации, явные ошибки и искажения географических названий исправлены 
        без оговорок, за исключением некоторых особо оговоренных случаев, когда 
        публикатор избегал этого в целях сохранения стиля документа. <br>
        <br>
