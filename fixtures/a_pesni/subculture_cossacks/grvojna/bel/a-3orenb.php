        <br>
        История большевизма в станице Коельской <br>
        <br>
        Как известно, захват большевиками власти в Русском государстве произошел 
        в последних числах Октября 1917 года, мы, казаки, до Декабря месяца имели 
        о них (большевиках — А.Г.) смутное понятие; хотя большевистские газеты 
        посылались в Коелгу<sup>49</sup> весьма исправно, но они не объявлялись 
        и мало читались. 25 Декабря Троицк был занят большевиками, в Коелге настроение 
        было тревожное, после расформирования Оренбургских полков на фронте, фронтовики 
        прибывали домой, продавая и разделяя между собою полковое имущество и 
        капиталы, настроение у них было <u>явно-большевистское и с тех</u><sup>50</sup> 
        же пор начались разногласия фронтовиков <u>со стариками</u> и даже открытая 
        вражда, а вместе с ней и двойная политика Коельского общества, так как 
        посылались делегаты на Войсковые круги, казачьи съезды, а также и на большевистский 
        Губернский Съезд и Уездный, депутаты привозили постановления, явно противоречащие 
        друг другу и наконец дошло до того, что фронтовики 21 Февраля<sup>51</sup> 

        собравшись, постановили: не признавать Войскового Круга и Атамана Дутова 
        и все постановления Круга считать необязательными...<sup>52</sup> Так 
        продолжалось до Марта месяца, к этому времени атмосфера сгустилась, большевики 
        владели всеми Оренбургскими городами<sup>53</sup>, казачество явно падало, 
        комиссар Цвилинг<sup>54</sup> в Оренбурге заявил «с этих пор казачество 
        не существует», того же мнения держался и комиссар города Троицка <u>Аппельбаум</u>... 
        Большевики стали посылать карательные отряды по упорствующим станицам, 
        тем не менее Коельцы 15 Mapтa<sup>55</sup> постановили защищать Казачество 
        и выступать против большевиков; единодушие было почти общее: избрали военный 
        К[омите]-т из 12 человек, решили ежедневно посылать отряды по числу сформирования 
        и на другой же день к вечеру отправили отряд из 42 человек под командою 
        2х местных офицеров Гостева и Первухина в станицу Кичигинскую на подмогу, 
        на другой день отправили второй отряд из 80 человек под командою урядника 
        Петра Матвеева, вооружение состояло из винтовок французского образца, 
        присланных Управлением Отдела в количестве 300 штук; такое формирование 
        продолжалось бы и далее, если бы первый отряд не возвратился по причине 
        от него не зависящей, так как, пришедши в Кичигину, куда его требовал 
        член войскового правительства Рудаков, отряд узнал, что Кичигинцы на сходе 
        решили не воевать и даже стали творить насмешки над нашим отрядом. Начальник 
        отряда Первухин послал делегацию в село Николаевку — какого там настроения 
        мужички? Там сообщили делегации, «что мы воевать не собираемся, ибо мы 
        Вас боимся». Благодаря такому обороту дела, отряд возвратился домой и 
        разоружился, второй же отряд возвратили с дороги от Кочкаря<sup>56</sup>, 
        который тоже разоружился... <br>
        <br>
        18 Марта с/ст.<sup>57</sup> получается /01/ приказ от Начальника Карательного 
        отряда большевиков товарища Блюхера со станции Еманжелинской, «что за 
        непризнание Советской Власти все агитаторы, организаторы и руководители 
        будут расстреляны». Собрали экстренное собрание, стали обсуждать создавшееся 
        положение; местные большевики подняли головы, винили стариков, что де 
        они вздумали воевать; называли их: «пустые головы, пустые бороды», старикам 
        пришлось выслушивать от своих детей всевозможные унижения! Некоторые [в]ставали 
        на колени и со слезами на глазах говорили: «простите нас, фронтовички, 
        ибо мы заблуждались». <br>
        <br>
        В это же время фронтовики арестовали 2 офицеров, находящихся в Коелге, 
        17<sup><u>го</u></sup> полка Потапова и Подкорытова. Наконец решили принять 
        Советскую Власть, выбрали наскоро председателя Совета и всю администрацию, 
        наконец решили послать делегацию к начальнику отряда Блюхеру с извинением 
        за прежние выступления и с протоколом о принятии Советской Власти. Охотников 
        ехать не находилось,... потому что уже знали, как большевики расправлялись 
        с виновными, наконец фронтовики нашлись: «Кому надо было воевать, тот 
        пущай и едет оправдывается, вон Попов, ему надо было войну, пущай он и 
        едет». А так как Вахмистр Попов был записан в книгу постановлений по имени, 
        что он и прочие ораторы высказались за войну с красной армией, ему и выпал 
        жребий ехать /я буду высказываться от себя, ибо я состою в комиссии по 
        составлению этого очерка, я ездил в делегацию, я же был на Войсковых Кругах 
        Декабрьском и Верхнеуральском/. Я ни слова протеста, но товарища мне долго 
        искали, так как все отказывались, наконец согласился ехать приказный Петр 
        Хохряков.<br>
        <br>
        Поехали сначала в поселок Ключи; где находится штаб Блюхера было неизвестно; 
        в Ключах узнали, что Блюхер находится на Кичигинском переезде, поехали 
        туда; там большевиков уже нет, проехали в Кичигину, там узнаем, что большевики, 
        исправив железную дорогу, сломанную казаками, прошли на станцию Нижнеувельскую, 
        едем дальше, нигде не встретили ни постов, ни караулов; на станции Нижнеувельской 
        стоят 3 состава поездов, тут были на платформах орудия, пулеметы, зарядные 
        ящики, сновали туда и сюда красноармейцы, нас окружили, спросив, что нам 
        нужно? Ответили, что нам нужно начальника отряда; указали вагон — зашли, 
        сначала пришлось долго ждать, потому что посетителей было много, и все 
        с разными жалобами; наконец очередь пришла и до нас. Блюхер спросил: «Что 
        Вам нужно?» Я ответил, что мы уполномоченные от Коельского общества, привезли 
        постановление о принятии Советской Власти, так же сообщил, что мы, Коельцы, 
        собирали отряды /против б[ольшевиков]/. Блюхер спросил: «Для чего Вы их 
        собирали?», я ответил: «Для самообороны». Блюхер взял наши удостоверения 
        о личности, долго просматривал лежащий на столе длинный список, а затем, 
        положив его на стол, долго пристально смотрел на меня, а затем, прочитав 
        протокол о принятии Советской Власти и все Копии с бумаг, по которым мы 
        выступали, в том числе и от Члена Войскового Правительства Рудакова, произнес: 
        «Вот до чего Ваше офицерство Вас доводит — Рудаков, кажется, пойман и 
        ваш Дутов скоро будет пойман». Затем Блюхер приказал под диктовку машинисту 
        писать следующее: «3 Апреля н/ст.<sup>58</sup> привезти на станцию Нижнеувельскую 
        из Коелги все огнестрельное оружие, какое имеется, за исключением дробовок. 
        Вместе с оружием привезти арестованных всех агитаторов, организаторов 
        и зачинщиков против Советской Власти, а также и все книги постановлений, 
        вместе с этим привести и 2-х арестованных офицеров». Я перервал (так в 
        тексте — А.Г.), говоря: «товарищ Блюхер офицеры уже отправлены в г. Троицк, 
        так как еще при нас был отправлен пакет об их отправке», в таком случае 
        привезите расписку в их сдаче. Дальше диктует: «если не будет выполнено 
        постановление от 18 марта о принятии Советской Власти, то станица Коельская 
        подвергнется той же участи, как и станица Донецкая под Оренбургом, Солодянка 
        под Троицком, Таяндами<sup>59</sup> и Кичигиной (так в тексте — А.Г.)». 
        На бумаге этой стоял штемпель Блюхера: «Командующий всеми восточными отрядами, 
        действующими против Дутова». Подавая эту бумагу, Блюхер сказал: «Торопитесь 
        в свою Коелгу да смотрите, чтобы ваше постановление было выполнено». Тут 
        мы догадались, что Таянды уже громят, да и на нашу Коелгу дано распоряжение. 
        Поехали мы обратно с невеселыми думами, так как к 3 Апреля мы должны быть 
        арестованы: я как главный агитатор, записанный в книгу постановлений по 
        имени, а Хохряков зачинщик; ночевали в Кичигиной, потому что уже было 
        поздно, утром дорога нам лежит через село Николаевку, через которую Блюхер 
        дал нам пропуск, въезжаем, из первой избы выбежало 3 человека мужичков, 
        вооруженных 3х линейными винтовками, спросили пропуск, комиссар проводил 
        нас на другую сторону села; приехали в Коелгу часов в 10 утра, собрали 
        экстренный сход, протолклись весь день, к вечеру стали прибывать люди 
        из поселков на юртовый сход, настроение было подавленное, потому что по 
        бумаге Блюхера должно быть арестовано не менее 24 человек, в довершение 
        всего из Таяндов приехал курьер и сообщил, что там большевики расстреляли 
        13-ть (так в тексте — А.Г.) самых лучших людей поселка...<br>
        <br>
        Часов в 10 вечера в коридоре закричали: «большевики пришли», все заметались, 
        но бежать было некуда, так как тут же послышался голос: «Не выходить, 
        стрелять будем». На площади чернелось несколько десятков красноармейцев 
        при шести пулеметах; все, конечно, сели на парты, потому что сход помещался 
        в школе. Входит молодой человек лет 26 в куртке, вооруженный с ног до 
        головы, и обращается к сходу: «Что у Вас за собрание?» <br>
        <br>
        Председатель отвечает: «Обсуждаем бумагу от начальника отряда Блюхера», 
        посмотрел и вышел — скоро входит старший отряда Комиссар Зверев при шашке 
        и револьвере, просмотрел бумагу и спросил: «Кто у Вас ездил к Блюхеру?», 
        мы с Хохряковым вышли, смерив нас глазами, Зверев сказал: «Хорошо, а где 
        у вас оружие?» Ему сказали, что собрано в другой комнате, пошел туда, 
        осмотрел, входит обратно и говорит: «Здесь наши, завтра дайте нам 50 подвод 
        до Варламовой /Верхнеувельской/, а сегодня разведите людей по квартирам 
        на ночлег». Потом стал писать бумагу начальнику оцепления, чтобы сняли 
        посты вокруг Коелги. Воспользовавшись этим временем, я, чтобы не быть 
        завтра арестованным, ночью бежал и скрывался от большевиков 2 с лишком 
        месяца в Камышах (так в тексте — А.Г.) озера Большой Сарыкуль в 4х верстах 
        от станции Еманжелинской. <br>
        <br>
        На другой день рано собрался сход, стали посылать за винтовками, узнали, 
        что Попов сбежал, поднялась тревога, послали гонцов в поселки и телеграммы 
        в ближайшие города о розыске и т.п., остальных лиц в количестве 23 человек 
        арестовали. Нужно заметить, что все это усердствовали наши Коельские большевики, 
        главным образом, фронтовики и некоторые лица из поселочников, большевики 
        же утром, как сказали, так и ушли в Верхнеувельскую, ничего не сделавши. 
        Особенное усердие в деле ареста проявил из фронтовиков известный большевик 
        Машин, который говорил, когда отправляли арестованных: «Главный виновник 
        сбежал, поэтому смягчится участь арестованных, поклянемся доставить его 
        труп», еще из фронтовиков главарями были урядник Матвеев, Коковин, казаки 
        Иван Хохряков и Плешков, из жителей 2 Сметанина и Коряков, а также некоторые 
        разночинцы. <br>
        <br>
        3<sup><u>го</u></sup> Апреля<sup>60</sup> утром отправили всех арестованных 
        на станцию Нижнеувельскую, вместе с ними для защиты поехал почти весь 
        сход в количестве 70 человек, на станции Нижнеувельской пришлось выслушивать 
        от большевиков всякие унижения, некоторые говорили: «Что с ними волочиться, 
        расстрелять их», но, к счастью, обошлось благополучно, арестованных отправили 
        в город Троицк на суд, а из числа жителей защитников выбрали шесть человек, 
        а остальных проводили домой. В Троицке судили и, благодаря защите некоторых 
        коельских фронтовиков, всех оправдали, за исключением 2 офицеров Гостева 
        и Первухина, которых присудили на 3 месяца в тюрьму. <br>
        <br>
        После этого переворота зажили Советской жизнью, более зажиточные люди, 
        боясь разориться, стали задабривать всю ленивую бедную свору, отдавая 
        то лошадь, то корову. По распоряжению местного совета наложили контрибуцию 
        на богатых сельчан в сумме 20,000 рублей. Эта контрибуция расходовалась 
        на содержание Совета и прочих советских властей поселка; расточительность 
        денег была ужасающая, так, например, годовую смету хотели утвердить в 
        90 тысящь (так в тексте — А.Г.) рублей, переведя все натуральные повинности 
        на денежные, и это должна выдержать станица из Зх поселков, тогда как 
        ранее, эта же станица, состоящая из 16 поселков, несла повинностей на 
        3 тысячи рублей. <br>
        <br>
        В довершение горя, ко дню прихода большевиков приехали делегаты с Губернского 
        Съезда и привезли о пуда проэкт (так в тексте — А.Г.) землеустройства 
        по-новому, т.е. вся земля трудовому народу; начались засевы земли у неимущих 
        богатыми бесплатно, так как бедные не в состоянии были засеять всей своей 
        земли, все разночинцы были уравнены в правах на ровне (так в тексте — 
        А.Г.) с казаками и получили доли земли и т.д. Наконец, услышали Чехо-Словацкий 
        переворот в Челябинске, большевики зашевелились; фронтовики крепко держали 
        свою власть в своих руках до тех пор, когда 23 мая Коелгу окружил с казачьим 
        отрядом хорунжий Кучин и, поставив пулеметы на гору к амбарам, тем принудил 
        отказаться от Советской власти. После этого началась ломка Советского 
        строя, был выбран атаман Зайков и прочее Казачье Управление; земли же, 
        засеянные казаками и разночинцами у прочих казаков, пришлось оплатить 
        по существующим ценам на землю и т.п. Для борьбы с большевиками стали 
        собираться добровольческие отряды, особенное усердие в этом проявил 55-летний 
        старик-доброволец Петр Ив[анович] Паньков, который участвовал при взятии 
        города Троицка и у которого на фронте было 2 сына, нельзя умолчать также 
        геройской смертью павшего урядника на Екатеринбургском фронте Владимира 
        Ивановича Панькова, храбрость которого засвидетельствована командиром 
        полка и жителями города Екатеринбурга. <br>
        <br>
        Некоторые из фронтовиков, как, например, урядник Матвеев, уже окупили 
        свою вину, сражаясь против большевиков на Верхнеуральском фронте. <br>
        <br>
        Как видит читатель, большевистский набег на Коелгу не прошел бесследно. 
        <br>
