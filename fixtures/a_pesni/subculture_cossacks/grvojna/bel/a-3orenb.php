        <strong>Донесение из станицы Крутоярской в окружное правление III военного 
        округа от 05.12.1918 г. № 826</strong><br>
        <br>
        В Окружное правление 3го округа <br>
        О[ренбургское]К[азачье] В[ойско] <br>
        КРУТОЯРСКАЯ <br>
        станичная управа <br>
        3-го Округа <br>
        <u>5 Дек[абря]</u> 1918 г. <br>
        № <u>826</u> <br>
        <br>
        Большевизм начал себя обнаруживать в 3м округе Оренб[ургского] (губ[ернии])<sup>6</sup> 
        Казачьего Войска и в частности в нашей станице с времени октябрьского 
        переворота, когда некоторые казаки и солдаты, приходя с фронта, говорили, 
        что не нужно войны с Германией, зачем она нам, что воюют буржуи, а нам 
        войны не нужно и утвердился после занятия советскими войсками города Троицка,<sup>7</sup> 
        когда казачьи части не хотели защищать свою территорию [и] разбежались 
        по домам. <br>
        <br>
        Эти казаки оправдывались тем, что они от большевиков ничего плохого не 
        видали и воевать против них не пойдут. В станице образовались две партии: 
        фронтовики, стоявшие за советскую власть и старики — сторонники казачества. 
        <br>
        <br>
        Яростными сторонниками советской власти являются казаки-фронтовики Александр 
        Новиков, Сергей Атмановский, Максим Тамгин, Алексей Беспалов, Григорий 
        Новиков, Григорий Закатунин, Иван Ястрибев (так в тексте — А.Г.), Яков 
        Коротков, но их покрыл <u>Николай Алексеев Беспалов</u><sup>8</sup> советский 
        комисар (тек в тексте — А.Г) и <u>главный агитатор</u>.<sup>9</sup> На 
        всех общественных собраниях эти люди всегда высказывали мысли вполне большевитские 
        (так в тексте — А.Г.), тащили стариков всеми мерами в советскую власть, 
        обзывали офицеров гадами в серебряных погонах. <br>
        <br>
        Иван Путилов, бывший вместе с Григорием Закатуниным на фронтовом съезде, 
        про окружное правление выразился так: давно их следовало перебить всех. 
        <br>
        <br>
        Яков Прокопич <u>Koротков</u>, будучи послан в город Троицк узнать в чем 
        дело и что за большевики, с восторгом говорил старикам, возвратившись 
        оттуда: «Старики, не бойтесь, нас хотят устроить очень хорошо, идите в 
        советскую власть». <u>Николай Беспалов</u> выступал на собраниях с своими 
        речами [и] всегда говорил: «Товарищи, пора взять власть в свои руки», 
        важно было то, что этот <u>Беспалов</u> записывал все, что говорилось 
        против советской власти, а затем доносил Троицким комисарам (так в тексте 
        — А.Г.), все сторонники советской или большевитской (так в тексте — А.Г.) 
        власти были организованы, видимо, где-то там в полках, на обучение их 
        много потрачено времени. Совсем другое представляла партия казачества; 
        здесь, главным образом, выступало два человека. Члены войскового круга 
        и окружного съезда Андрей Иванов Клюев [и] Михаил Евдокимов Кошкаров, 
        — эти два лица отстаивали самобытность казачества, руководствуясь в своей 
        деятельности указаниями войскового круга, трудились от души, даже, как 
        потом оказалось, с опасностью для своей жизни: «Предать их военно-полевому 
        суду», - говорили большевики, масса сторонников была<sup>10</sup> на их 
        стороне. Когда большевики прислали повестку дать представителя на съезд 
        Крестьянских, казачьих, солдатских депутатов,<sup>11</sup> то старики 
        составили наказ, в котором не признали советской власти, а признали войсковой 
        круг. <br>
        <br>
        Послали двух делегатов: Семена Курнакина и Александра Артемьева, из двух 
        избранных людей оказалось, что Артемьев вернулся домой и объявил обществу 
        о том, что на собрании их заставляли их (так в тексте — А.Г.) силой признать 
        советскую власть, там все делалось вопреки данному наказу, почему работать 
        на съезде он не мог и вернулся домой. Семен же Курнакин сошелся с большевиком 
        Николаем <u>Беспаловым</u> и был на съезде не с товарищем Артемьевым, 
        а с большевиком Беспаловым Николаем. Приехав домой, объявил старикам, 
        что наказ нужно изменить, надо признать советскую власть. <br>
        <br>
        В это время случилось выступление казаков против большевиков. В других 
        станицах при объявлении похода был большой подъем духа, все радостно собирались, 
        хлопотали, но в нашей станице не верили в войну казаков с большевиками. 
        На общем собрании, несмотря на усиление членов Клюева и Кошкарова, часть 
        фронтовиков не дала<sup>12</sup> возможности сформировать свои отряды, 
        а решила<sup>13</sup> послать делегатов Карюшина и Закатунина в г. Троицк 
        и узнать, что там происходит. <br>
        <br>
        Посланные привезли известие о войне казаков. Была тревога, народ взволновался, 
        собрался на площадь, где было устроено общее собрание. Опять-таки часть 
        фронтовиков все меры употребила, чтобы не дать возможности формировать 
        отряды. <br>
        <br>
        Атмановский Сергей говорил: «Товарищи, дайте мне коня, все обмундирование, 
        обеспечьте жену, сына, всю домашность, тогда я согласен идти на войну». 
        Николай Ефремов Андронов не хочет идти на войну, «возьмите все мое имущество, 
        но на войну я не пойду», большинство казаков шло против войны. Здесь отстаивали 
        казачество те же лица: Андрей Клюев и Михаил Кошкаров. На вопрос о средствах 
        ведения войны Клюев сказал, что войсковое правительство выпускает внутренний 
        заем, на который он и записывает 50 рублей. Большевик Александр Новиков 
        настаивал держать нейтралитет, на что получил ответ от Михаила Кошкарова, 
        который призывал казачество объединиться вокруг войскового правительства, 
        а на предложенный нейтралитет Новикова сказал так: «Станичники, помните, 
        когда Пилат предавал Христа, то тоже держал нейтралитет». <br>
        <br>
        Это потрясающе подействовало на сход, все, кроме части фронтовиков, были 
        согласны идти на войну, но в самый разгар, председатель вдруг закрыл собрание. 
        Впечатление сменилось. Между тем, проходили эшалоны (так в тексте — А.Г.) 
        казаков из других станиц для защиты казачества, наши станичные большевики 
        смеялись над ними, спрашивая: «Товарищи, на кого идете воевать?», но старики-станичники 
        призывали собраться и дать отпор большевикам. Павел Ильич Зотов говорил 
        так: «Я видел сборы казаков в других станицах, имею 45 лет от роду, согласен 
        идти в поход против большевиков. На что большевик Шишов Егор ответил: 
        «Бог тебя благословит, первая тебе пуля в лоб» и бывшая тут часть фронтовиков 
        заявила, что даже если бы кто и пожелал [идти] против большевиков, то 
        они не пустят, если же кто уедет, пусть не возвращается в станицу, так 
        наша станица и осталась не выступающей и под игом большевизма жила пять 
        (5) месяцев. Тяжело было положение казаков при советской власти и при 
        освобождении г. Троицка<sup>14</sup> ехали и свободно вздыхали, на предложение 
        явиться в ряды войска охотно пришли чехам на помощь. <br>
        <br>
        Атаман станицы <br>
        Урядник Кашкаров. <br>
