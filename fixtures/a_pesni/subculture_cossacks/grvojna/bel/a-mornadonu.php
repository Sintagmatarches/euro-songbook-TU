	<br><span class="text"><span class="header"><strong>ИЗ 
  ИСТОРИИ УЧАСТИЯ МОРЯКОВ В ГРАЖДАНСКОЙ ВОЙНЕ НА ДОНУ</strong><br></span>
  <br>
  Воспоминания А. Богаевского, 1932, И. Кононова, 1957, А. Штрома, 1984. Публикация, 
  предисловие и примечания С.Ю. Гордеева и Н.А. Кузнецова<em><br>
  <br>
        Альманах «Белая гвардия», №8. Казачество России в Белом движении. М., 
        «Посев», 2005, стр. 57-66.<br>
  <br>
  <br>
  Как известно, многочисленные морские формирования в период Гражданской войны 
  воевали практически с каждой из противоборствующих сторон. Немалое их число 
  было сформировано и на юге России, в частности на Дону. На страницах «Белой 
  Гвардии» уже был опубликован ряд статей и документов, посвященных этой тематике. 
  Сегодня мы хотим предложить читателям ряд новых материалов. Это — работа Войскового 
  атамана Всевеликого войска Донского (ВВД) А.П. Богаевского и воспоминания военных 
  моряков — контр-адмирала И.А. Кононова и старшего лейтенанта А.А. Штрома. <br>
  <br>
  Личность <strong>Африкана Петровича Богаевского (27.12.1872 - 21.10.1934)</strong> 
  достаточно хорошо известна всем интересующимся историей Белого движения. По 
  происхождению Богаевский был дворянином войска Донского, сыном офицера - казака 
  станицы Каменской. Он окончил Донской кадетский корпус в 1890 г., Николаевское 
  кавалерийское училище в 1892 г., академию Генштаба в 1900-ом. А.П. Богаевский 
  был офицером Лейб-Гвардии Атаманского полка. В Первую Мировую войну — генерал-майор 
  (1915 г.), начальник 1-й гвардейской кавалерийской дивизии, Георгиевский кавалер. 
  В Донской армии с января 1918 г. Африкан Петрович командовал войсками Ростовского 
  района. Дальнейший его послужной список выглядит следующим образом: участник 
  1-го Кубанского («Ледяного») похода: командир Партизанского полка и с 17 марта 
  1918 г. - 2-й бригады; служба в Донской армии; с 4 мая 1918 г.- председатель 
  Совета управляющих отделами ВВД (Донского правительства) и управляющий иностранным 
  отделом; с 6 февраля 1919 г. - Войсковой атаман Донского войска (эту должность 
  он исполнял до самой смерти). 27 августа 1918 г. Богаевский был произведен в 
  чин генерал-лейтенанта. В эмиграции он жил в Софии (с ноября 1921 г.), Белграде 
  (с октября 1922 г.), и Париже (с ноября 1923 г.), где и скончался<sup>1</sup>. 
  Публикуемая впервые статья была написана им в эмиграции, в настоящее время она 
  хранится в фонде 6461 (Канцелярия Донского атамана. 1920—1942) ГА РФ. <br>
  <br>
  Контр-адмирал <strong>Иван Анатольевич Кононов (25.12.1885 - 23.01.1959)</strong> 
  также происходил из дворян войска Донского. Он родился в Кронштадте в семье 
  контр-адмирала А.А. Кононова. В 1905 г. И.А. Кононов окончил Морской корпус, 
  в 1909 г. — Штурманский офицерский класс, в 1911 г. — военно-морское отделение 
  Николаевской морской академии(по 1-му разряду). В период Первой Мировой войны 
  Кононов служил на Черном море. В 1916 г. он был произведен в чин капитана 1-го 
  ранга. Последняя его должность на 1917 г. — командир строящегося крейсера «Адмирал 
  Нахимов». В Белом движении Иван Анатольевич участвовал с ноября 1917 г. Первоначально 
  Кононов числился в распоряжении Войскового атамана, затем - помощником командующего 
  Донской армией по морской части, начальником военно-морского управления Донской 
  армии (март—май 1919 г.). Приказом Главнокомандующего ВСЮР от 7 июня 1919 г. 
  за №1148 И.А. Кононов был произведен за отличие в контр-адмиралы, приказом от 
  27 июня 1919 г. он был назначен начальником речных сил Юга России. По некоторым 
  данным, Кононов был произведен и в вице-адмиралы. Но последнего производства 
  он не признавал, до конца жизни во всех документах подписываясь как контр-адмирал. 
  В эмиграции Кононов жил и скончался в Париже<sup>2</sup>. Его воспоминания были 
  впервые опубликованы в парижском журнале «Возрождение». <br>
  <br>
  Лейтенант <strong>Александр Александрович Штром (17.12.1892 - 16.10.1989)</strong> 
  родился в Санкт-Петербурге, окончил ростовскую-на-дону мужскую гимназию в 1911 
  г. и Санкт-Петербургский университет в 1916 г. После этого Александр Александрович 
  поступает в Школу мичманов военного времени, откуда выпускается 30 мая 1917 
  г. с чином мичмана военного времени, заканчивает краткие курсы морской артиллерии. 
  После этого попадает на батарею №38, находившуюся на острове Даго и входившую 
  в состав Приморского фронта Морской крепости Императора Петра Великого. После 
  захвата Моонзундских островов германскими войсками, А.А. Штром попадает в плен, 
  откуда возвращается на родину лишь в августе 1918 г. В Белом движении Штром 
  участвует в рядах Донской армии, ВСЮР и Русской армии. В 1919 г. он исполнял 
  обязанности ревизора на канонерской лодке «К-16». С апреля 1920 г. и вплоть 
  до самой эвакуации Крыма, после производства в лейтенанты, последовавшего 26 
  апреля 1920 г., занимал должность артиллерийского офицера эскадренного броненосца 
  «Ростислав». В эмиграции 1920 по 1944 гг. жил в Югославии, с 1948 г. — в Аргентине, 
  скончался в Буэнос-Айресе. Он активно занимался публицистической деятельностью, 
  был сотрудником журнала «Военная быль». В 1993 г. дочь А.А. Штрома Анна Александровна 
  через заместителя главного редактора журнала «Наше Наследдие» А.М. Кузнецова 
  передал архив своего отца в Россию, где он пополнил фонды Центрального военно-морского 
  музея в Санкт-Петербурге.<sup>3</sup> Воспоминании публикуются впервые по ксерокопии 
  авторской рукописи, находящейся в личном архиве Н.А. Кузнецова.<br>
  <br>
  Необходимо отметить, тот факт, что на первый взгляд публикуемые работы (особенно 
  это касается материалов Богаевского и Кононова) дают достаточно полную, хоть 
  и беглую картину участия моряков в Белой борьбе на Дону. Однако необходимо учитывать, 
  что все публикуемые произведения были созданы уже в эмиграции, причем при их 
  написании авторы не могли пользоваться большим количеством документов, основная 
  масса которых осталась в России. Поэтому значительное количество неточностей 
  в их трудах публикаторы постарались исправить в подстрочных примечаниях, написанных 
  на основе изучения архивных материалов. <br>
  </em><br>
  <br>
        <strong>Донской Атаман. Работа моряков на Дону (ноябрь 1917 г. — декабрь 
        1919 г.)</strong><br>
  <br>
  В журнале «Часовой» от 15 ноября 1931 г. помещена статья: «Белый Флот на Юге 
  России»<sup>4</sup>. Автор статьи почти что не коснулся работы моряков на Дону 
  за время Белого движения, хотя эта работа была очень большая и заслуживает беспристрастной 
  оценки и она должна быть ярко отмечена, хотя бы во имя исторической истины и 
  для ознакомления тех, кто в этот тяжелый период героической борьбы горсточки 
  доблестных моряков не был на Дону и на Юге России. <br>
  <br>
  В конце октября 1917 г. начали стекаться на Дон все русские люди, которым честь 
  России была дорога и которые не пожелали склонить свои головы перед узурпаторами 
  власти, Лениным и Троцким. <br>
  <br>
  Среди пришедших на Дон начали появляться и морские офицеры. Первые прибывшие 
  в Новочеркасск из моряков были адмирал Анатолий Кононов<sup>5</sup>, лейтенант 
  Кононов<sup>6</sup>, лейтенанты Данилов<sup>7</sup> и Поздеев<sup>8</sup>. Все 
  они родом были донцы. <br>
  <br>
  В ноябре 1917 г., 4-го числа, в Новочеркасск генералом Алексеевым была создана 
  Добровольческая Армия. <br>
  <br>
  В течение ноября на Дон прибыли инженер-мех[аник] капитан 2 ранга Герасимов<sup>9</sup>, 
  адмирал Иван Кононов, капитан 1 ранга Шуберт<sup>10</sup>, Потемкин<sup>11</sup>, 
  лейтенанты Энвальд<sup>12</sup>, Кунаков<sup>13</sup>, Елкин<sup>14</sup> (последние 
  двое — также донцы). <br>
  <br>
  Первым доблестным делом моряков это была (так в тексте — прим. публ.) экспедиция 
  из Старочеркасска на рыбачьих лодках вниз по Дону для захвата яхты «Колхида»<sup>15</sup> 
  и бронированной баржи с одним 6-ти дюймовым орудием Канэ и с одним 120-мм [орудием] 
  Канэ<sup>16</sup>. Впоследствии эти оба орудия были сняты с баржи и поставлены 
  на железнодорожные платформы<sup>17</sup>. <br>
  <br>
  В 1918 г. [контр-]адмирал<sup>18</sup> Иван Кононов был назначен помощником 
  Командующего Донской Армии. <br>
  <br>
  В течение 1918 г.<sup>19</sup> на Дону была сформирована Донская флотилия под 
  командой к[апитана] 2 р[анга] Герасимова (утонул в реке). <br>
  <br>
  К июлю командующим флотом был назначен генерал-лейт[енант] флота К. Оглоблинский<sup>20</sup>, 
  а в декабре — адмирал Фабрицкий<sup>21</sup>. <br>
  <br>
  К весне 1919 г. адмиралом Кононовым было сформировано на Дону: <br>
  <br>
  1) 7 дивизионов боевой флотилии<sup>22</sup>, причем речные канонерские лодки 
  были вооружены 6[-ти] дюймовыми орудиями Канэ (перевернутыми компрессорами кверху, 
  что увеличило дальность стрельбы до 16S верст)<sup>23</sup>, 120[-]мм [орудиями] 
  Канэ и 3-х дюймовыми;<br>
  <br>
  2) Транспортная флотилия<sup>24</sup> под командой к[апитана] 1 р[анга] Щепотьева<sup>25</sup>; 
  <br>
  <br>
  3) Особый отряд Азовского моря<sup>26</sup>, под командой Доблестного капитана 
  1 ранга Собецкого<sup>27</sup>, состоявший из 2-х дивизионов и вооруженный также 
  6 дюйм[овыми], 120[-]мм и 75[-]мм [орудиями] Канэ; <br>
  <br>
  4) Отдельный морской батальон<sup>28</sup>;<br>
  <br>
  5) 5 Дивизионов Морской Тяжелой Артиллерии<sup>29</sup> на ж[елезно]д[орожных] 
  платформах с орудиями, начиная от 8 дюймов, в 52 калибра длиною; <br>
  <br>
  6) 1 дивизион танковый с орудиями в 5,2 дюймов<sup>30</sup>. <br>
  <br>
  К концу мая 1919 г. Дон уже был освобожден от большевиков и, за время борьбы 
  с красными, мощная организация моряков под начальством адмирала Кононова, оказала 
  неоцененные услуги Донской армии<sup>31</sup>. <br>
  <br>
  Все руководители у адмирала были моряки, но весь личный состав, как на флотилиях, 
  так и во всех Дивизионах Мор[ской] Тяж[елой] Артиллерии, были исключительно 
  казаки<sup>32</sup>. <br>
  <br>
  В июле 1919 г. адмирал Кононов был назначен приказом Главнокомандующего генерала 
  Деникина - Командиром Отдельного Корпуса и Старшим Флагманом, отдельно командующим, 
  Черного моря<sup>33</sup>. <br>
  <br>
  Адмирал продолжал интенсивно формировать и разворачивать свой корпус и к августу 
  1919 г. корпус<sup>34</sup> уже состоял из: <br>
  <br>
  1) 9 дивизионов Мор[ской] Тяж[елой] Артиллерии. <br>
  <br>
  2) 4 танковых дивизионов<sup>35</sup><br>
  <br>
  3) 5-ти речных флотилий: <br>
  а) Донская<sup>36</sup>, под командой адмирала Фабрицкого; <br>
  б) Волжская<sup>37</sup>, под командой адмирала Заева<sup>38</sup>; <br>
  в) Верхнее-Днепровская<sup>39</sup> — кап[итан] 1 р[анга] Лукомский<sup>40</sup>; 
  <br>
  д) Средне-Днепровская<sup>41</sup> — адмирал Г.И. Бутаков<sup>42</sup>; <br>
  ж) Нижнее-Днепровская - кап[итан] 1 р[анга] Собацкий <br>
  <br>
  4) Транспортная флотилия — адмирал Денисов<sup>43</sup>; <br>
  <br>
  5) 1-ый Морской полк под командой полковника по адмиралтейству Тарасевича<sup>44</sup>, 
  георгиевского кавалера. Погиб геройской смертью при обороне Перекопа. Этот полк 
  осенью 1919 [г.] развернулся в 4-х полковую Морскую Дивизию<sup>45</sup>; <br>
  <br>
  6) В Таганроге была создана военно-морская база<sup>46</sup>, во главе которой 
  стоял генерал флота К. Оглоблинский. Были мобилизованы все заводы Таганрога<sup>47</sup>, 
  которые своей беспрерывной работой и дали возможность адмиралу Кононову почти 
  из ничего сделать такой сильный корпус; <br>
  <br>
  7) Таганрогский порт был превращен в военный порт<sup>48</sup> и командиром 
  порта был адмирал Иванов-Тринадцатый<sup>49</sup> (герой боя 1-го августа 1904 
  г.). <br>
  <br>
  Инспектором Артиллерии<sup>50</sup> Корпуса был кап[итан] 1 ранга Подгорный<sup>51</sup>, 
  проявивший огромную энергию по созданию Морской Тяжелой Артиллерии на ж[елезно]д[орожных] 
  платформах и танковых дивизионов. <br>
  <br>
  Все части корпуса адмирала Кононова были составлены почти целиком из казаков. 
  <br>
  <br>
  Так как вся Добровольческая Армия имела очень мало тяжелой артиллерии, то все 
  дивизионы адмирала Кононова были распределены по всему фронту Добрармии и своей 
  мощной артиллерией, прекрасно организованной, оказали очень большую помощь армиям. 
  <br>
  <br>
  Среди моряков у адмирала Кононова были еще следующие офицеры, доблестно работавшие 
  на Дону, защищая честь своей Родины — это были: капитаны 1 ранга - Ромашев<sup>52</sup>, 
  Любимов, Гернет<sup>53</sup>; капитаны 2 ранга - Бодиско<sup>54</sup>, Горленко, 
  Драшусов<sup>55</sup>, Магнус<sup>56</sup>, Елкин, Данилов, Максимович<sup>57</sup>, 
  Соловьев<sup>58</sup>; старшие лейтенанты — Селезнев<sup>59</sup>, Кияшкин<sup>60</sup>, 
  Ермолов, Головин, Чихачев<sup>61</sup> и т. д. Инженеры: Иванов, Барков<sup>62</sup>, 
  Гончаренко, Воронов, Красицкий и т. д. <br>
  <br>
  Я, как Донской Атаман, могу с гордостью констатировать, что моряки — донцы в 
  самую тяжелую минуту бытия России слетелись со всех морей на Тихий Дон и [с] 
  самого начала белого движения начали свою энергичную работу, ведя отчаянную 
  борьбу с врагами великой России. <br>
  <br>
  5.01.1932 г. <br>
  Париж. <br>
  <em>ГА РФ. Ф. 6461. Оп. 1. Д. 7. Л. 1-8. </em><br>
  <br>
  <br>
        <strong>Кононов И.А. Казаки-моряки (Времен гражданской войны)</strong><br>
  <br>
  Морская стихия не была чуждой казакам и, когда это требовалось, они сменяли 
  своих коней на ладьи, срубы, а подчас и корабли и вступали в сражения с врагами 
  значительно сильнейшими по составу флота. <br>
  <br>
  Донская летопись полна рассказов о предпринятых казаками морских походах и в 
  Черном море, и в Азовском, и в Каспийском. А было, что казаки доходили и до 
  Северного Ледовитого океана, и до Тихого океана, переправлялись по Охотскому 
  морю на Камчатку. <br>
  <br>
  В XIX столетии казаки появляются уже как кадровые офицеры в Императорском флоте, 
  и число их к моменту первой мировой войны достигло до 40 офицеров. <br>
  <br>
  Когда Россию поразила ужасная «бескровная» революция и совершился октябрьский 
  переворот, то все казаки-моряки потянулись на юг по своим станицам и хуторам, 
  не желая примкнуть к захватившим власть большевикам. <br>
  <br>
  Пишущий эти строки прибыл на Дон, в Новочеркасск 4-го ноября 1917 г. и поступил 
  в распоряжение Донского Атамана. При Войсковом штабе был образован Военно-Морской 
  отдел, во главе которого он и был поставлен. <br>
  <br>
  Не только все казаки-моряки, но и вообще все морские офицеры, гардемарины и 
  кадеты, по прибытии на Дон являлись в Военно-Морское управление и поступали 
  в его распоряжение. Прибыли даже и матросы, не желавшие примкнуть к революции, 
  - правда их было не так много, но все же они были. <br>
  <br>
  Военно-Морское управление принялось энергично формировать Донскую флотилию, 
  Азовский отряд канонерских лодок, и, кроме того, броневые поезда, бронированные 
  автомобили и создало прототип танков с пятифунтовыми английскими пушками. <br>
  <br>
  Моряки-казаки появились тогда и на реках, и на море, на железной дороге и на 
  сухопутном фронте юга России. <br>
  <br>
  ***<br>
  <br>
  Для того чтобы воплотить в жизнь всю программу по созданию флотилий, поездов, 
  автомобилей и танков необходимо было произвести огромную работу по привлечению 
  ряда заводов Ростовского промышленного округа. Трагедия была в том, что на заводах 
  большинство рабочих примкнуло к большевикам и, покинув заводы, ушло на север. 
  Заводы пустовали и бездействовали. Раньше чем их пустить в ход, надо было создать 
  кадры специалистов и найти рабочих. Это было чрезвычайно трудно. <br>
  <br>
  Зима 1917-18 года была неспокойной, — шли бои по всему Дону. Регулярная работа 
  по выполнению намеченной программы была невозможна. <br>
  <br>
  Трагическая кончина атамана генерала Каледина ухудшила положение на Дону. Только 
  с весны 1918 года, когда пост атамана занял генерал П.Н. Краснов, жизнь на Дону 
  начала постепенно входить в нормальное русло, и моряки смогли приступить к осуществлению 
  намеченных работ. Дружно принялись за дело, и скоро речная Донская флотилия 
  приняла реальные формы. <br>
  <br>
  Речные пароходы вооружались трехдюймовой артиллерией и пулеметами, а на большие 
  баржи с дизелями<sup>63</sup> были поставлены морские шестидюймовые орудия, 
  которые пришлось доставить из Севастополя, где они были сняты с кораблей. В 
  результате, на Дону образовалась сильная флотилия, которая весною 1919 года 
  дошла до верховьев Дона и своей мощной артиллерией сослужила большую службу 
  донцам для изгнания большевиков из донских пределов. <br>
  <br>
  Параллельно с формированием речной флотилии в Таганроге создавалась Азовская 
  флотилия<sup>64</sup>, задача которой была очистка берегов Азовского моря от 
  большевиков. Для этой цели был привлечен по военно-морской повинности ряд пароходов, 
  на которых были установлены морские дальнобойные орудия 6 дюймов, 120[-]миллиметровые 
  и трехдюймовые, а также пулеметы. <br>
  <br>
  Одновременно с этими работами на заводах в Таганроге, строились бронепоезда 
  с 6 дюймовыми, 120[-] и 75[-]миллиметровыми орудиями, и были специально созданы 
  железнодорожные платформы, на которых были установлены дальнобойные восьмидюймовые 
  орудия. Все эти орудия также были привезены из Севастополя с соответствующим 
  боевым запасом снарядов. <br>
  <br>
  Тогда же морскими инженерами были построены как бы прототипы танков, для чего 
  были взяты тракторы типа «Ломбард» с гусеницами и на них установили английские 
  орудия пятифунтовые (4,2 дюйме). Скорость хода трактора была до 12 верст в час, 
  что давало ему возможность следовать с конницей. Так как тракторы не были забронированы, 
  то для их охраны придавались, в виде конвоя, по два бронированных автомобиля 
  с пулеметами и 37 миллиметровым орудием. Таким образом, боевой единицей была 
  группа из танка и двух броневых автомобилей<sup>65</sup>. Для вооружения танков 
  потребовалось добиться от англичан выдачи нам указанных орудий и боевого запаса. 
  <br>
  <br>
  В течение осени и зимы 1918-19 гг. вышеуказанная программа постепенно воплощалась 
  в жизнь, и боевые части, одна за другой, вступали в строй.<br>
  <br>
  Благодаря сказочной энергии моряков все технические трудности были преодолены, 
  заводы начали работать и личный состав для флотилий, бронепоездов, танков и 
  бронированных автомобилей бы создан. <br>
  <br>
  ***<br>
  <br>
  Когда «бескровная» революция начала потоками крови заливать необъятные пространства 
  Poссии, моряки-казаки постепенно сосредоточились на Дону. В это время в Области 
  Войска Донского никаких военно-морских организаций не существовало, и морякам 
  надо было из ничего создать все технические организации, и они их создали. К 
  весне 1919 года донские технические части оказались самыми большими на Юге России. 
  <br>
  <br>
  В октябре 1917 г. в Новочеркасск прибыл мой отец, адмирал Анатолий Алексеевич 
  Кононов, и был зачислен в личное распоряжение атамана Каледина. При атамане 
  Краснове на адмирала была возложена специальная миссия войти в связь с союзным 
  командованием в Севастополе<sup>66</sup>, чтобы пригласить на Дон представителей 
  союзного командования. Эту задачу адмирал и выполнил. <br>
  <br>
  Прибыв в Севастополь, он узнал, что командующим английским флотом состоит вице-адмирал 
  лорд Колторп, который в былые годы командовал стационером и стоял в бухте Суда 
  на Крите, где находилась и наша канонерская лодка «Храбрый», которой командовал 
  тогда капитан 2 ранга А.А. Кононов. Оба командира подружились, ездили вместе 
  на охоту и зачастую один у другого обедали. Когда адмирал Кононов прибыл на 
  дредноут адмирала Kолторпа, то последний его встретил как старого друга, что 
  и помогло выполнить миссию, возложенную атаманом. В распоряжение адмирала А.А. 
  Кононова были даны два миноносца: один английский, а другой французский. Командир 
  английского миноносца был старше французского, а посему - дипломатичен миссия 
  и была возложена на него и он, как представитель союзного командования, должен 
  был явиться к атаману и вступить с ним в переговоры. <br>
  <br>
  Миноносцы с адмиралом Кононовым прибыли в Мариуполь, где уже находился атаманский 
  поезд и генерал Свечин<sup>67</sup>, - представитель атамана. <br>
  <br>
  Во дворце атамана в Новочеркасске была устроена торжественная встреча союзников. 
  Было сказано много речей и трубачи исполнили три гимна: «Боже Царя храни», «Марсельезу» 
  и английский гимн. <br>
  <br>
  На следующий день в атаманском поезде генерал Краснов повез иностранных моряков 
  на фронт. Моему отцу нездоровилось и он просил меня его заменить и состоять 
  при союзниках. <br>
  <br>
  Рано утром поезд подошел к станции Богучаров. Пушистый снег белым ковром покрыл 
  перрон станции. Морозило, но ветра не было. Восходящие лучи зимнего солнца заливали 
  станцию и безграничную чтепь. Около подъезда станции стояли несколько саней, 
  запряженных рысаками, покрытыми разного цвета сетками. Атаман вышел на подъезд. 
  Ему подали вороного рысака. Он предложил англичанину сесть в сани вместе с ним. 
  В остальные сани уселись гости и все приехавшие с атаманом. В этот момент из-за 
  станционного сарая на рысях появился взвод казаков. Атаман поздоровался с ними 
  и приказал кучеру трогать. Все сани гуськом понеслись по белоснежной степи, 
  залитой розоватыми лучами восходящего солнца. Казаки с двух сторон окружили 
  атамана. <br>
  <br>
  В Богучарах была устроена союзникам пышная встреча. Но во время обеда слышалась 
  отдаленная канонада полевой артиллерии: большевики повели наступление, но были 
  отброшены донцами. <br>
  <br>
  К вечеру атаманский поезд двинулся в обратный путь и пошел в Новочеркасск. <br>
  <br>
  Атаман передал союзникам все свои нужды в смысле военного материала, авиации 
  и интендантского снабжения. <br>
  <br>
  На следующий день союзники, совместно с моим отцом отбыли через Мариуполь в 
  Севастополь. <br>
  <br>
  Адмирал Колторп, получив рапорт от командира своего миноносца, обещал адмиралу 
  А.А. Кононову снабдить Дон всем материалом, о котором написал ему атаман Краснов 
  и, надо отдать ему справедливость, он сдержал свое обещание, щедрой рукою снабжал 
  казаков боевыми припасами. <br>
  <br>
  К великому сожалению, это снабжение через три месяца прекратилось, так как Ллойд 
  Джордж был «недоволен оказываемой помощью казакам» и наложил свое вето на снабжение. 
  Лорд Колторп пытался протестовать, но был переведен на Дальний Восток и заменен 
  другим адмиралом. Об этом эпизоде с английским премьер-министром адмирал Колторп 
  лично рассказывал моему отцу. <br>
  <br>
  ***<br>
  <br>
  Работа моряков на Дону быстро развивалась. Из скромного Военно-Морского отдела 
  при Войсковом штабе она вылилась в две самостоятельных организации крупного 
  порядка. В 1919 г. приказом Главнокомандующего были созданы Речные Силы Юга 
  России и образованы речные флотилии: Верхне-Днепровская, под командой капитала 
  1 ранга Лукомского, Средне-Днепровская, под командой контр-адмирала Г.И. Бутакова, 
  - Нижне-Днепровская, под командой капитана 1 ранга В.И. Собецкого, - Волжская, 
  под командой контр-адмирала А.А. Заева и существовавшая уже Донская флотилия 
  под командой контр-адмирала С.С. Фабрицкого. <br>
  <br>
  Азовская флотилия, блестяще выполнившая свою задачу по очистке берегов Азовского 
  моря от большевиков, созданная выдающимся капитаном 1 ранга Собецким, была целиком 
  переброшена на нижний Днепр, где и послужила ядром создания Нижне-Днепровской 
  флотилии. <br>
  <br>
  Параллельно с Речными Силами был создан Отдельный Морской корпус, в состав которого 
  вошли все бронированные поезда, танки, бронированные автомобили, под названием 
  Морская Тяжелая Артиллерия, а также сформированная черноморская дивизия (четырехполкового 
  состава). Морской Артиллерией командовал капитан 1 ранга Я.И. Подгорный, а дивизией 
  - контр-адмирал Фабрицкий, который к тому времени уже сдал командование флотилией. 
  <br>
  <br>
  Во главе обеих организаций был поставлен автор этой статьи и назначен начальником 
  Речных Сил Юга России и командиром отдельного корпуса. <br>
  <br>
  Для характеристики боевой службы Речных Сил и Отдельного Корпуса приведем выдержку 
  из приказа Атамана А.П. Богаевскаго: <em>«В наикратчайший срок были созданы, 
  буквально из ничего, сильная флотилия и морская тяжелая артиллерия на поездах, 
  которые своею боевой работой на фронтах сослужили большую пользу Тихому Дону, 
  помогли армии в борьбе с красными и своею стрельбою из морских тяжелых орудий 
  сохранили много казачьих жизней...» <br>
  <br>
  «Правильно организованные морские части на Дону, спаянные железной дисциплиной, 
  во время общего наступления на север, разбросались по всему югу России, где 
  донские кадры были тем ядром, тем скрепляющим цементом, на котором, как на прочном 
  фундаменте, быстро создавались новые морские организации...». <br>
  <br>
  «От лица службы я искренно благодарю весь личный состав Речных Сил и Отдельного 
  Корпуса за их самоотверженную боевую работу, за их любовь и верность России 
  и за их преданность традициям седой старины и заветам Всевеликого Войска Донского». 
  </em><br>
  <br>
  Так казаки на морях и реках защищались от большевиков. <br>
