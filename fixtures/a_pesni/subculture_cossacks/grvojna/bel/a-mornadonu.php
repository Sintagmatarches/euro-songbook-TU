  Рано утром на следующий день я был на ж[елезно]д[орожной] станции в Ростове, 
  где узнал, что в ближайшие часы нет ни одного поезда на Таганрог. Когда я показал 
  командировочное предписание командира кан[онерской] лодки «К-16» о служебной 
  поездке, то начальник станции предложил мне воспользоваться идущим в ближайшее 
  время в Таганрог паровозом, на площадке которого я и отправился в Таганрог, 
  где по предъявлении необходимых документов и получил следуемые деньги и в тот 
  же, а м[ожет] б[ыть] и на другой, день вернулся в Ростов, но уже на каком-то 
  буксирном пароходе, название которого испарилось из моей памяти. <br>
  <br>
  Вернувшись в Ростов, я был направлен ст[аршим] л[ейтенантом] Оффенбергом к месту 
  стоянки моей лодки, на берегу Дона около завода Пастухова. <br>
  <br>
  Найдя лодку, я нашел там работу в разгаре — штурманская рубка обшивалась толстым 
  листовым железом. В носовой части, над кают-компанией, шел настил под 3 орудие 
  с лафетом. На корме устанавливалось 75 мм [орудие] судового типа на тумбе, под 
  наблюдением артиллерийского офицера И дивизиона лейт[енанта] Щербачева<sup>70</sup>. 
  <br>
  <br>
  В тот же день лодка вышла на позицию, а я остался в Ростове для получения в 
  пекарне заказанного хлеба и закупки необходимой посуды и мелкого инвентаря. 
  В помощь мне остался кондуктор Долгов (из штаба дивизиона), очень смышленый 
  и расторопный человек, который мне очень помог. <br>
  <br>
  На другой день мы с ним на паровом катере «Татарин», погрузив хлеб и закупленный 
  инвентарь, [отправились на свой корабль]. <br>
  <br>
  Благодаря весеннему половодью [...], Дон [...] очень разлился, т.е. фарватер 
  нельзя было даже угадать. Нам пришлось идти «на ура!» и катер один раз приткнулся 
  к мелкому месту с которого благополучно выбрался. <br>
  <br>
  К Новочеркасску подошли уже в сумерки, и потому я решил переночевать в городе, 
  в штабе флотилии, который помещался в здании духовной семинарии. Придя туда, 
  я вспомнил, что это страстной четверг и в домовой церкви идет богослужение XII 
  евангелий, на которое я отчасти запоздал, но все-таки успел прослушать несколько 
  евангелий. <br>
  <br>
  Тут же в семинарии я и переночевал и на другой день в страстную пятницу я догнал 
  мою посудину, если не ошибаюсь, около стан[ицы] Семикаракорской. На следующий 
  день, в страстную пятницу, мы ходили вверх по Дону, вернее — огромному озеру. 
  <br>
  <br>
  Ночь провели на так называемом «сашиме» - т.е. достаточно толстом бревне, вместо 
  «битенга»<sup>71</sup>. <br>
  <br>
  Как мы провели страстную субботу, точно вспомнить не могу. Вспоминаю, что около 
  какой-то станицы на левом берегу Дона, по просьбе жителей этой станицы, была 
  произведена экзекуция — т.е. телесное наказание. Если память мне не изменяет, 
  то его [наказываемого] просто отлупили «шомполом», т.е. металлическим прутом, 
  который служит для чистки ствола винтовки. <br>
  <br>
  На другой день Св[ятой] Пасхи мы где-то нагнали канон[ерскую] лодку «Кубанец», 
  если не ошибаюсь — [бывший] пассажирский колесный пароход. Командиром его оказался 
  Вася Красовский, мой соученик по гимназии. Поэтому праздник у него на лодке 
  провели хорошо и изрядно выпили.<br>
  <br>
  В тот же (или на следующий) день [на] «K-16» мы направились в Ростов. Уже подходя 
  к Ростову, лодка оказалась на каком-то перекате, т.е. наносном мелком месте 
  на фарватере реки. В результате лодка перестала слушаться руля. При осмотре 
  оказалось, что лопнул шток руля. При более подробном осмотре специальная комиссия 
  (командир, штурман и механик) выяснила, что в месте слома штока уже раньше была 
  поломка, которая была сварена. До Ростова нас дотащили на буксире. Нужно было 
  чинить поломку. Вызвали мастера от фирмы Нитнера (у него же был магазин на Б[ольшой] 
  Садовой улице, где продавались книги и почтовые марки для коллекции). Специалист 
  от фирмы, после осмотра поломки, заявил, что вторично производить сварку поломанного 
  штока нет смысла, а надо заново отлить всю раму руля со штоком. Это было принято 
  к сведению. Нитнер прислал специалистов, которые и сняли как раму руля, так 
  и поломанный шток и все это [...] отправили в мастерскую, где с поврежденного 
  штока и пера руля (довольно большая и основательная штука) сняли точную копию 
  по которой и произвели отливку рамы руля. Вся эта комбинация заняла много времени, 
  так что лодка осталась пришвартована к набережной, а мы занялись мелкими работами. 
  <br>
  <br>
  Так, к имевшейся одной короткой мачте приделали стеньгу и рею. Флагшток для 
  кормового флаги перенесли на гакаборт и укрепили его в несколько наклоненном 
  к гакаборту положении. Наш флаг, с изображением в крыже, [я] взял домой и на 
  швейной машинке заменил крыж с орлом Всев[еликого] войс[ка] Донского<sup>72</sup> 
  верхней четвертой частью Андреевского флага [...]. На мачте и рее провели флаг-фал 
  с каждого борта и за [так в тексте, следует читать — «во», прим. публ.] время 
  пребывания на лодке адмирала С.С. Фабрицкого был поднят к[онтр]-адм[иральский] 
  флаг, а на ноке рея поднимался сигнальный флаг «ОН» - за [так в тексте, следует 
  читать — «во», прим. публ.] время обеда и отдыха. <br>
