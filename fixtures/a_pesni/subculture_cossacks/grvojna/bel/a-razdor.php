	<span class="text"><strong>Л.А. Молчанов</strong><br>
  <br>
       <span class="header"><strong>«РАЗДОР, РАСКОЛ – ЭТО ЛИ НАЗЫВАЕТСЯ 
        СПАСЕНИЕМ РОССИИ?»</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 80-87</em>.<br>
  <br>
  <br>
  Перед антибольшевицким движением России в годы Гражданской войны стояли две 
  важнейшие задачи: достижение военно-политического единства антибольшевицких 
  сил и мобилизация масс на борьбу с Советской властью. Как показал опыт войны, 
  согласованно решить эти задачи оказалось очень сложным делом: слишком острыми 
  были противоречия между антибольшевицкими силами, и слишком низок был уровень 
  политической организации российского общества. Особенно очевидно эти проблемы 
  проявились на белом Юге России, где накал политических страстей и ожесточенность 
  борьбы с большевиками были особенно велики. <br>
  <br>
  В тот период для мобилизации масс на борьбу с большевиками правительствами многих 
  антибольшевицких политических режимов принимались законодательные акты, провозглашавшиеся 
  их составителями конституционными. Они появлялись в период, когда российская 
  государственность переживала острейший кризис. Эти документы носили временный, 
  переходный и декларативный характер. В них законодатели показывали основы будущего 
  устройства России, которые должны были вызвать симпатии общества. <br>
  <br>
  Одним из подобных законов была Конституция Кубанского края 1918 г. (ее полное 
  название - Временное положение об управлении Кубанским краем). Она отражала 
  соотношение социально-политических сил в крае, сложившееся к моменту ее разработки 
  и принятия. Конституция юридически закрепляла переходный этап на пути Кубани 
  к парламентской демократии. Будущее Кубани и будущее России кубанские законодатели 
  видели в федеративной республике. Эта федерация должна была быть построена по 
  территориальному принципу, т.к., по мнению политических деятелей края, жизнь 
  показала, что национальные принципы «не служат признаками... для того, чтобы 
  государство было построено на федеративных началах»<sup>1</sup>. <br>
  <br>
  Конституция появилась в условиях жесткого противостояния и борьбы за власть 
  между кубанским казачеством и Добровольческой армией. С лета 1918 г. по мере 
  освобождения Кубани от большевиков отношения между властями Кубанской области 
  и деникинским руководством обострялись. Tecные союзнические отношения между 
  кубанцами и добровольцами, достигнутые в ходе совместной борьбы с Красной армией, 
  стали портиться. Слишком сильно расходились их политические доктрины. Деникин 
  как руководитель Вооруженных Сил на Юге России обоснованно полагал, что на время 
  боевых действий вся полнота власти в регионах, занимаемых его армиями, должна 
  принадлежать армейскому командованию. Он являлся выразителем идеи восстановления 
  единства России. Этот процесс на белом Юге, по его мнению, должен был происходить 
  вокруг Добровольческой армии. Деникинское командование доказывало, что освобождение 
  Кубани неразрывно связано с освобождением всей России. Для успешной борьбы с 
  большевиками на юге России нужна сильная армия и единое правительство. Создание 
  во время войны независимого государственного образования на территории Кубани 
  с верховной властью казачьего правительства ослабит борьбу. В области государственного 
  управления политики Особого совещания предлагали оставить дореволюционный статус 
  казачьих земель и требовали подчинения Кубани «Временному положению об управлении 
  областями, занимаемыми Добровольческой армией». Выступая на заседании Чрезвычайной 
  Краевой думы, генерал А.С. Лукомский подчеркивал, что на белом Юге необходимо 
  создавать такую власть, «которая дала бы возможность окончить борьбу с большевиками, 
  возможно скорее, возможно энергичнее»<sup>2</sup>. <br>
  <br>
  Деникин и его окружение полагали, что кубанские федералистские течения не имеют 
  глубоких корней в казачестве, не пользуются симпатиями населения области и потому 
  не представляют большой силы. Это был политический просчет, свидетельствовавший 
  о плохом знании деникинским командованием общественных настроений в казачьих 
  областях. Как показало развитие событий, эти идеи, в значительной степени, отвечали 
  подлинным настроениям и устремлениям кубанских казаков. Кубанские политики и 
  общественность боялись реставрации старых порядков. На территории края, по свидетельству 
  председателя краевого правительства Л.Л. Быча, наиболее массовыми настроениями 
  были желания самим «устраивать свою судьбу, решать вопросы своей жизни»<sup>3</sup>. 
  <br>
  <br>
  Кубанское казачество также стремилось к восстановлен единства России, но оно 
  видело будущее государства в виде федерации. По мнению политиков края, пока 
  в России не установится законная власть в лице Учредительного собрания, Кубанская 
  область должна существовать как независимое, самостоятельное государство. Один 
  из самых активных членов Краевой Рады П.Л. Макаренко постоянно подчеркивал, 
  что «Кубань является штатом, впредь до воссоздания единой власти через Учредительное 
  собрание, штатом самостоятельным»<sup>4</sup>. Кубанские политические лидеры 
  выступали против всех проектов, предлагаемых командованием Добровольческой армии 
  и основанных на сосредоточении в руках Деникина всей полноты власти. <br>
  <br>
  Однако внутри кубанского общества не было единства по поводу государственного 
  статуса области. Между казаками существовало разделение на две более или менее 
  определенные политические группировки: «линейцев» и «черноморцев». «Линейцами» 
  были казаки южных отделов (районов) области, в основном выходцы с Дона. Они 
  выступали за компромисс с Особым совещанием. Своей важнейшей задачей «линейцы» 
  считали объединение всех казаачьих регионов юга России «в прочный государственный 
  союз казачьих земель Кубани, Дона и Терека»<sup>5</sup>. Этот союз должен был 
  стать центром объединения других государственных образований и областей юга 
  России под общим командованием Добровольческой армии. <br>
  <br>
  «Черноморцами» назвались жители нижнего течения Кубани и побережья Азовского 
  моря, потомки запорожцев. К ним примыкала «небольшая, но активная часть кубанских 
  горцев... которая увлекалась тогда идеями горского сепаратизма»<sup>6</sup>. 
  «Черноморцы» были наиболее последовательны в федералистских намерениях. Их лидеры 
  стремились к созданию Южнорусского союза, построенного на началах федерации. 
  Этот союз должен был стать прообразом будущего российского государства. Предполагалось, 
  что на правах членов в этот союз должны были войти казачьи области Кубани, Терека 
  и Дона, а также Украина и некоторые государства Закавказья. Каждый член федерации 
  должен был иметь свое правительство и армию. Во главе федерации должен был находиться 
  Верховный союзный совет, состоящий из представителей правительств областей федерации, 
  которому должен был подчиняться командующий армией союза. Каждый член федерации 
  из состава своих войск выделял «по особому расписанию, войсковые части, всецело 
  подчиненные верховному командованию»<sup>7</sup>. <br>
  <br>
  Нагнетанию напряженности в Кубанском крае способствовало то, что постепенно 
  большинство и в Краевой Раде и краевом правительстве переходило к «черноморцам», 
  а в политическую борьбу оказался втянутым ряд высших военных чинов Кубанского 
  казачьего войска. Некоторые военачальники недвусмысленно намекали на то, что 
  они откроют фронт большевикам, если не будет достигнуто соглашения с Деникиным. 
  <br>
  <br>
  Дальнейшее обострение противостояния властей Кубанского Края с Особым совещанием 
  было связано с избранием и деятельностью Чрезвычайной Краевой Рады Кубанского 
  края, высшего органа представительной власти области. Выборы, проведенные осенью 
  1918 г., принесли победу казакам и крестьянам. Их насчитывалось 60% от общего 
  состава Рады. 25% членов Рады являлись офицерами, 15% представляли другие прослойки 
  казачьей интеллигенции. Депутаты объединились во фракции в основном по территориальному 
  принципу. Всего насчитывалось 9 групп: Лабинского, Кавказского, Екатеринославского, 
  Таманского, Ейского, Майкопского и Баталпашинского отделов. Отдельные группы 
  составляли горцы и представители Добровольческой армии. Все это дало основание 
  некоторым членам Рады говорить о политической слабости кубанского парламентаризма. 
  «У нас партий нет, - говорилось на заседании Краевой рады 20 ноября. - У нас 
  совершенно своеобразный парламент, который разбивается не на партии, а на отделы»<sup>8</sup>. 
  <br>
  <br>
  Одним из первых шагов Краевой Рады было принятие декларации, определяющей статус 
  Кубанской области. В ней провозглашалась суверенность края. В знак протеста 
  командование Добрармии отозвало из Рады своих представителей. Окончательный 
  разрыв был временно отложен избранием Радой Войсковым атаманом генерала А.П. 
  Филимонова — сторонника «линейцев». <br>
  <br>
  В таких условиях председателем краевого правительства Л.Л. Бычем на втором заседании 
  Чрезвычайной Рады Кубанского края, состоявшемся 29 октября 1918 г., впервые 
  был поставлен вопрос о необходимости подготовки конституции Кубанской области. 
  К этому времени кубанский государственный строй все более отчетливо приобретал 
  формы своеобразного казачьего парламентаризма, которому конституция должна была 
  дать законченное выражение. Кубанские парламентарии считали, что кубанская конституция 
  должна быть «составлена в духе принципов демократического казачества»<sup>9</sup>. 
  Она должна зафиксировать форму настоящей организации власти в Кубанской области 
  и дать ответ на вопрос о месте Кубани в будущем Российском государстве после 
  ожидаемой победы над большевиками. Подчеркивая готовность кубанского общества 
  к принятию демократических принципов, члены Краевой Рады подчеркивали, что «между 
  Кубанским краем и Северо-Американскими Соединенными штатами есть кое-что, и 
  даже много, общего»<sup>10</sup>. <br>
  <br>
  Первоначальный проект новой конституции был выработан в правительства Кубанского 
  края. Для дальнейшего рассмотрения проект был передан в комиссию Чрезвычайной 
  Краевой Рады по самоуправлению. Она подготовила свою редакцию документа, которая 
  отличалась от правительственного проекта. Большие споры в комиссии вызвал вопрос 
  о том, кто же может быть Войсковым атаманом. Высказывались мнения, что главою 
  края может быть любой человек, много сделавший для края, любящий его и хорошо 
  знающий проблемы его народа. Другие говорили, что атаманом края может быть только 
  казак, который родился и вырос на кубанской земле. В результате бурных споров 
  комиссией была принята формулировка, вводившая для должности атамана ценз по 
  военной службе. В проекте появилась статья о том, что войсковым атаманом может 
  быть не только природный, но и приписной казак, прослуживший краю не менее 15 
  лет. <br>
  <br>
  Зрелый государственный подход проявила комиссия при обсуждении проблем организации 
  местной власти в крае. В частности, бурно дебатировался вопрос об избрании в 
  каждом отделе края региональной представительной и исполнительной власти в лице 
  местной Рады и атамана. Это в перспективе подрывало целостность Кубанской области 
  и снижало эффективность действия государственного аппарата. Комиссия не поддалась 
  достаточно широко распространенным в то время популистским настроениям и решила 
  этих институтов не вводить. Большим испытанием на прочность демократических 
  намерений кубанского общества явилось обсуждение в комиссии вопроса о выборах 
  в Законодательную Раду, которая должна была выполнять функции высшей представительной 
  власти в перерывах между работой Краевой Рады. Часть членов комиссии настаивала 
  на том, чтобы парламентарии Законодательной Рады избирались только из числа 
  делегатов Краевой Рады. Правительственное предложение сводилось к всенародному 
  избранию постоянно действующего кубанского парламента. В конце концов, победили 
  демократические требования и идея всенародных выборов в парламент. При этом 
  не все принятые комиссией Рады решения нашли отражение в окончательном тексте 
  Конституции. <br>
  <br>
  Отдельные предложения конституции обсуждались в согласительной комиссии, созданной 
  по договоренности с Деникиным для разрешения спорных вопросов. В состав комиссии 
  входили: члены кубанского правительства: Ф.С. Сушков, Д.Е. Скобцев, А.А. Намитоков, 
  А.А. Макаренко, члены Кубанской Рады С.Ф. Манжула, Ю.А. Коробьин и др. Интересы 
  Добровольческой армии представляли генерал-лейтенант Лукомский, полковник Туненберг, 
  адвокат В.Ф. Зеелер, члены Особого совещания: Н.И. Астров, К.Н. Соколов и др. 
  В ходе работы комиссии представители Особого совещания в очередной раз попытались 
  добиться от кубанцев признания власти Главного командования Вооруженных Сил 
  на Юге России. И в очередной раз эта идея вызвала возмущение со стороны казаков. 
  Оказавшись не в состоянии договориться от общих принципах, добровольцы и кубанцы 
  искали спасения в решении конкретных вопросов. Но к каждому из них делегации 
  подходили со своей принципиальной точки зрения. <br>
  <br>
  Принципы были непримиримо различны, поэтому попытки договориться закончились 
  неудачей. Среди множества проблем, обсуждавшихся на заседаниях комиссии, одними 
  из важнейших являлись вопросы о кубанском атамане, кубанской армии и международной 
  деятельности кубанских властей. Кубанские парламентарии были против того, чтобы 
  в конституции было зафиксировано подчинение атамана Главнокомандующему Вооруженными 
  Силами на Юге России, и настаивали на конституционном положении о создании собственной 
  армии. Кроме этого, представители Рады считали, что раз Кубань — «не штат, а 
  как Дон, суверенное государство»<sup>11</sup>, то кубанские власти должны получить 
  конституционное право заключать международные военные и торговые договоры. <br>
  <br>
  Представители Особого совещания выступали против этих положений, так как они 
  нарушали единство управления армиями и вели к снижению их боеспособности. Но 
  представители кубанцев твердо стояли на своем и не шли на уступки. В ходе работы 
  согласительной комиссии никаких существенных изменений в проект конституции 
  внесено не было. Такая позиция Краевой Рады еще больше обострила отношения между 
  кубанскими властями и деникинским командованием. <br>
  <br>
  5 декабря 1918 г. на 33 пленарном заседании Чрезвычайной краевой Рады Конституция 
  была принята в третьем чтении. Ее принятие было временной победой федералистской 
  политики кубанского руководства в его борьбе с Особым совещанием. Конституция 
  была небольшой по объему и состояла из преамбулы и XI разделов, каждый из которых 
  включал от 1 до 17 статей (всего 62 статьи). <br>
  <br>
  Исторические условия появления на свет Конституции в значительной степени предопределили 
  ее социально-политическую сущность, содержание и форму. Она включала основные 
  положения об управлении краем, определяла компетенцию высших законодательных 
  и исполнительных органов власти, права и обязанности граждан. Ее содержание 
  в корне расходилось с «Временным положением об управлении областями, занимаемыми 
  Добровольческой армией». Такая ситуация делала практически невозможным ее применение 
  и еще больше обостряла отношения между командованием Добровольческой армией 
  и кубанскими властями. <br>
  <br>
  Конституция Кубанского края создавалась с перспективой ее возможной переработки 
  и корректировки в будущем с учетом новых исторических условий. Поэтому она фиксировала, 
  прежде всего, политическое положение дел на текущий момент. Принятием Конституции 
  кубанские власти пытались решить вопрос о разграничении функций, полномочий 
  и компетенции органов власти, а также законодательно закрепить положение Кубанского 
  края на антибольшевицком Юге России. Объектами регулирования являлись организация 
  государственной власти (система высших органов государства, их организация, 
  компетенция, взаимоотношения) и правовой статус личности, ограниченный гражданскими 
  и политическими правами и свободами. <br>
  <br>
  Конституция Кубанского края провозглашала создание в крае демократического государства. 
  Источником государственной власти объявлялась «воля его граждан, выражаемая 
  на представительных собраниях»<sup>12</sup>. Конституция проводила принцип разделения 
  властей. Носителями законодательной власти в крае являлись Краевая и Законодательная 
  Рады. Высшей исполнительной властью являлись Войсковой атаман и краевое правительство. 
  Судебная власть осуществлялась независимыми судами. <br>
  <br>
  В компетенцию Краевой Рады входило избрание Войскового атамана, а также рассмотрение 
  и утверждение «законов, вносящих изменения в основные положения об управлении 
  краем», а также решение вопросов, «вносимых на ее рассмотрение войсковым атаманом, 
  или Законодательной радой по постановлению двух третей всех ее членов или вопросов, 
  возбужденных по почину самой Краевой рады во время ее сессии»<sup>13</sup>. 
  Краевая Рада созывалась или Войсковым атаманом, или по решению Законодательной 
  Рады, принятому квалифицированным большинством голосов. По разрешению вопросов, 
  ради которых Рада созывалась, полномочия ее считались прекращенными. Законодательные 
  акты, принятые Краевой Радой, были окончательными и не требовали утверждения 
  Войсковым атаманом. <br>
  <br>
  Законодательная Рада избиралась на три года. Она пользовалась широкими правами. 
  Рада осуществляла законодательную деятельность на постоянной основе и пользовалась 
  правом законодательной инициативы. Члены Pады могли принимать участие в заседаниях 
  Краевой Рады с правом совещательного голоса. К предметам ведения Законодательной 
  Рады относились вопросы принятия и утверждения краевого бюджета и проектов правительственных 
  займов, определения ежегодного контингента призывников, ратификация международных 
  договоров, оценка деятельности краевого правительства. На заседании Законодательной 
  Рады выносилась роспись краевых расходов и доходов. <br>
  <br>
  Как правило, заседания Краевой и Законодательной Рад должны были носить публичный 
  характер, а решения приниматься простым большинством голосов. Рабочими органами 
  сессий обеих Рад были президиумы в составе председателей, их товарищей и секретарей. 
  Краевая и Законодательная Рады могли образовывать комиссии, которые пользовались 
  «правом требовать устные и письменные объяснения и справки от членов правительства, 
  всех правительственных и других учреждений и должностных лиц»<sup>14</sup>. 
  <br>
  <br>
  По конституции, Краевая и Законодательная Рады объявлялись неприкосновенными. 
  Члены Рад за мнения, «высказываемые при исполнении ими своих обязанностей», 
  не несли «никакой ответственности ни в обще уголовном порядке, ни в порядке 
  частного обвинения»<sup>15</sup>. Во время сессий члены Краевой и Законодательной 
  Рад «без разрешения соответствующего представительного собрания», не могли «подлежать 
  личному задержанию или судебному преследованию, за исключением случая задержания 
  на месте преступления»<sup>16</sup>. Члены Законодательной Рады не могли занимать 
  «платные должности по назначению правительственной власти, а также выборные 
  административные или полицейские должности за исключением членов правительства, 
  краевого контролера и их товарищей»<sup>17</sup>. <br>
  <br>
  Главой Кубанского края был Войсковой атаман. Ему были подчинены вооруженные 
  силы края. Он избирался Краевой Радой на четыре года «из числа природных кубанских 
  казаков и горцев, а также из числа приписных казаков, прослуживших в Кубанскому 
  краю не менее пятнадцати лет»<sup>18</sup>. Вступая в должность, атаман клялся 
  «верно и нелицеприятно» служить Кубанскому казачеству и населению Кубанского 
  края, «и ради славы и процветания его не щадить своего живота до последней капли 
  крови»<sup>19</sup>. Атаман имел право законодательной инициативы. Он производил 
  назначения на военные и Гражданские должности. <br>
  <br>
  Войсковой атаман назначал председателя и членов правительственного кабинета. 
  К его компетенции относилось утверждение законов, принятых Законодательной Радой. 
  Он имел право вето и мог не утвердить законопроект. В этом случае проект возвращался 
  назад в Раду для нового рассмотрения. Однако если законопроект после вторичного 
  рассмотрения квалифицированным большинством голосов Рады принимался вновь, утверждения 
  атамана уже не требовалось, и закон должен был быть обнародован в трехдневный 
  срок. Специфика правового положения атамана заключалась в том, что его полномочия 
  были ограничены главою краевого правительства. Все акты атамана должны были 
  быть скреплены подписями председателя правительства или отдельных членов правительства 
  по принадлежности. Без этого указы атамана были недействительны. Председатель 
  краевого правительства мог временно исполнять обязанности атамана в «случае 
  тяжкой болезни или выезда Войскового атамана за пределы Кубанского края»<sup>20</sup>. 
  <br>
  <br>
  Кроме Войскового атамана, исполнительную власть в Кубанском крае осуществляло 
  краевое правительство «в составе председателя и членов, составляющих совет правительства»<sup>21</sup>. 
  Совет правительства нес солидарную ответственность перед Законодательной Радой 
  за свою деятельность. Председатель и члены правительства пользовались правом 
  свободного доступа на заседания Краевой и Законодательной Рад. В то время, когда 
  Законодательная Рада не функционировала, совет правительства пользовался «правом 
  издания законов», которые требовали утверждения Войсковым атаманом. Но эти законы 
  подлежали утверждению Законодательной Рады. Последняя могла отправить правительство 
  в отставку простым большинством голосов. <br>
  <br>
  Согласно конституции, достаточно большими полномочиями наделялся краевой контролер, 
  который избирался Законодательной Радой и имел право присутствовать на заседаниях 
  Краевой и Законодательной Рад. Он должен был следить за исполнением краевой 
  росписи доходов и расходов и осуществлять контроль «над финансово-хозяйственной 
  деятельностью всех органов исполнительной власти края»<sup>22</sup>. <br>
  <br>
  Анализ закрепленных в конституции прав и свобод граждан свидетельствует о ее 
  демократической направленности. Она гарантировала гражданам права и свободы, 
  которые обеспечивали им правовую возможность активного участия в общественной 
  и государственной жизни. Конституцией провозглашалась свобода совести, неприкосновенность 
  личности и жилища, свобода слова и печати, собраний и союзов. Однако на положение 
  о гражданских свободах в крае оказывали воздействие условия Гражданской войны. 
  В конституции оговаривалась возможность временного введения цензуры «при чрезвычайных 
  обстоятельствах в законодательном порядке»<sup>23</sup>. <br>
  <br>
  Ограниченность предмета конституционного регулирования в конституции сказалась 
  на слабой проработке положений о гражданстве Кубанской области. В нем говорилось, 
  что все «граждане Кубанского края равны перед законом», а все «российские граждане 
  пользуются в Кубанском крае защитою закона на общих с гражданами Кубанского 
  края основаниях»<sup>24</sup>. Эта нечеткая формулировка не давала ясного правового 
  ответа на вопрос, являются ли граждане края гражданами России. Кроме того, в 
  конституции отсутствовали статьи о предоставлении прав гражданства на Кубани, 
  несмотря на то, что вопрос о предоставлении кубанского гражданства активно дискутировался 
  в период подготовки Конституции. В тексте основного закона лишь подчеркивалось, 
  что права кубанского краевого гражданства «определяются особым законом»<sup>25</sup>. 
  Такое не до конца продуманное отношение кубанских законодателей к проблеме гражданства 
  являлось свидетельством невысокой политической культуры общественности области, 
  стремящейся с демократии, но еще не умеющей жить в демократических условиях. 
  Отсутствие четкости в определении гражданства Кубанского края не позволило законодателям 
  прописать в конституции избирательные права граждан края и порядок избрания 
  Краевой и Законодательной Рад. Это должно было быть сделано на основании особых 
  законов Краевой Рады. Только в примечании к статье 15 конституции указывалось, 
  что выборы членов первой Законодательной Рады производятся сроком на один год 
  Чрезвычайной краевой Радой созыва 28 октября 1918 г. в количестве 80 человек 
  «из числа членов Чрезвычайной краевой рады пропорционально количеству населения 
  каждого отдела»<sup>26</sup>. <br>
  <br>
  Конституция Кубанского края оставляла многие элементы государственности за пределами 
  своего внимания, предоставляя их регулирование обычному законодательству или 
  практике. В ней ничего не говорилось об административном делении территории 
  края, не называлась система местных органов власти, форма взаимоотношений центральной 
  и местной власти. Вне рамок конституционного регулирования оставались экономические 
  и социальные отношения. В Конституции не было статей, определявших экономическую 
  основу края. Кубанские законодатели не сочли необходимым хотя бы частично зафиксировать 
  эти положения в конституции, несмотря на то, что в ходе работы Краевой Рады 
  вопрос об экономике края обсуждался и был подготовлен специальный законопроект, 
  касавшийся прав собственности и основ землепользования. <br>
  <br>
  В конституции отсутствовали также специальные статьи, определявшие обязанности 
  граждан. Исключением была воинская повинность. В конституции говорилось, что 
  все «граждане Кубанского края подлежат воинской повинности, отбываемой на основании 
  особого закона»<sup>27</sup>. Однако условия выполнения воинской повинности, 
  связанные с призывным возрастом, сроком службы и т.д., в конституции не оговаривались. 
  <br>
  <br>
  При этом необходимо отметить, что кубанские парламентарии во многом видели недостатки 
  конституции и всего местного законодательства. Поэтому они ввели в Конституцию 
  раздел IX, в котором говорилось о том, что в крае сохраняют силу законы Российской 
  империи и Временного правительства 1917 г., «поскольку те и другие касаются 
  кубанского края и поскольку они не отменены и не изменены высшей краевой властью»<sup>28</sup>. 
  Однако данная статья содержала в себе достаточно глубокое противоречие. Законы 
  Российской империи носили самодержавный характер и не всегда могли обеспечить 
  выполнение демократических положений конституции. <br>
  <br>
  Кубанские законодатели очень гордились своей конституцией и считали, что она 
  «глубже и демократичнее, чем конституции, существующие на Западе или в Америке»<sup>29</sup>. 
  Однако после принятия Конституции отношения между Особым совещанием и кубанским 
  и властями продолжали обостряться, а взаимоотношения между ними становились 
  «все более и более непарламентскими»<sup>30</sup>. Развязка наступила в ноябре 
  1919 г., когда Кубанская Рада была разогнана Деникиным, а в отношении наиболее 
  видных деятелей кубанской оппозиции были применены репрессии. Подобное развитие 
  политических отношений между командованием Вооруженных Сил на Юге России и кубанским 
  казачеством, на наш взгляд, было одной из главных причин, приведших «к катастрофическому 
  отходу вооруженных сил Юга России от Орла до Новороссийска»<sup>31</sup>. <br>
  <br>
  Конституция Кубанского края является важным памятником политической истории 
  России и дает богатый материал для изучения регионального законотворчества в 
  годы Гражданской войны. Ее подготовка и содержание показывают, что отсутствие 
  в России гражданского общества не позволило населению юга России сплотиться 
  в борьбе с большевиками. Это же обстоятельство являлось главной причиной того, 
  что военные и политические руководители антибольшевицких режимов были слабо 
  готовы к объединению в борьбе с советским авторитарным режимом. Антагонизм усугублялся 
  жаждой власти военных и политических лидеров. Стремление к демократическим преобразованиям 
  обгоняло реализацию насущных потребностей борьбы с большевизмом, усугубляло 
  раскол и никак не способствовало выходу России из кризиса. <br>
