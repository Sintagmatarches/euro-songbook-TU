  цели, помятуя, что нет счастья народного без закона, правды и свободы. Боже 
  Милосердный и Истинный! Рука Твоя благая да укажет мне путь правый, ведущий 
  к пользе государственной и к величию Кубанского края, во главе которого я поставлен 
  волею Краевой рады. Помоги мне в великом служении моем. Аминь. <br>
  <br>
     <em>Конституция Кубанского края 1918 г. Прага, 1929. </em><br>
