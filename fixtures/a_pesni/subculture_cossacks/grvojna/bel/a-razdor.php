        <strong>Приложение<br>
  <br>
        <font color="#990000" size="4">КОНСТИТУЦИЯ КУБАНСКОГО КРАЯ 1918 г. </font></strong><br>
  <br>
  РАЗДЕЛ I. <br>
  О территории. <br>
  <br>
  1. Территория Кубанского края образуется из Кубанской области бывшей Российской 
  империи. <br>
  <br>
  2. Границы этой территории не могут быть изменены иначе, как с согласия кубанской 
  Краевой рады. <br>
  <br>
  РАЗДЕЛ II. <br>
  Общие положения. <br>
  <br>
  3. Все граждане Кубанского края равны перед законом. Права Кубанского краевого 
  гражданства определяются особым законом. Все российские граждане пользуются 
  в Кубанском крае защитою закона на общих с гражданами Кубанского края основаниях. 
  <br>
  <br>
  4. Все граждане пользуются свободой совести. Никто не может быть преследуем 
  за религиозные убеждения, и никто не может пользоваться привилегиями на вероисповедных 
  основаниях. <br>
  <br>
  5. Личность и жилище неприкосновенны. Никто не может быть подвергнут аресту, 
  или обыску, или вторжению в жилище иначе, как в случаях и в порядке, законом 
  определенных. <br>
  <br>
  6. Устанавливается свобода слова и печати. Цензура может быть вводима временно, 
  только при чрезвычайных обстоятельствах в законодательном порядке. <br>
  <br>
  7. Гражданам представляется свобода coбpaний и союзов с соблюдением правил, 
  установленных особым законом. <br>
  <br>
  8. Кубанский край имеет свою армию. Все граждане Кубанского края подлежат воинской 
  повинности, отбываемой на основании особого закона. <br>
  <br>
  РАЗДЕЛ III. <br>
  Источник и органы высшей власти. <br>
  <br>
  9. Источником высшей власти в Кубанском крае является воля его граждан, выражаемая 
  на представительных собраниях. Носителями законодательной власти в Кубанском 
  крае являются Краевая и Законодательная рады, а носителями исполнительной власти 
  — войсковой атаман и Кубанское краевое правительство. Судебная власть осуществляется 
  независимыми судами, действующими на основаниях, в законе определенных. Судебный 
  решения и приговоры выносятся именем закона. <br>
  <br>
  РАЗДЕЛ IV. <br>
  Краевая рада. <br>
  <br>
  10. Состав Краевой рады и порядок ее избрания определяются особым законом, принятым 
  Краевой радой. <br>
  <br>
  11. Краевая рада созывается для избрания Войскового атамана и для рассмотрения 
  и утверждения законов, вносящих изменения в основные положения об управлении 
  краем, а также для решения вопросов, вносимых на ее рассмотрение Войсковым атаманом 
  или Законодательной радой по постановлению двух третей всех ее членов, или вопросов, 
  возбужденных по почину самой Краевой рады во время ее сессии. <br>
  <br>
  12. Краевая рада созывается Войсковым атаманом по его инициативе или по решению 
  Законодательной рады, принятому большинством двух третей всех ее членов. По 
  разрешению Краевой радой вопросов, для коих она была созвана, а равно возбужденных 
  по ее инициативе, полномочия ее считаются прекращенными. <br>
  <br>
  13. Для открытия сессии Краевой рады необходимо прибытие не менее двух третей 
  членов ее. <br>
  <br>
  14. Краевая рада сама проверяет полномочия своих членов. <br>
  <br>
  РАЗДЕЛ V. <br>
  Законодательная рада. <br>
  <br>
  15. Законодательная рада избирается на три года на основании особого закона, 
  принятого Краевой радой. <br>
  <br>
  Примечание: избрание первой по принятию настоящего положения Законодательной 
  рады производится сроком на один год Чрезвычайной Краевой радой созыва 28 октября 
  1918 г. в количестве восьмидесяти человек из числа членов Чрезвычайной Краевой 
  рады пропорционально количеству населения каждого отдела. Чрезвычайная Краевая 
  рада избирает также кандидатов к членам Законодательной рады в числе сорока 
  человек. <br>
  <br>
  16. Законодательная рада осуществляет законодательную деятельность, поскольку 
  она не отнесена к ведению Краевой рады, и пользуется правом законодательной 
  инициативы. По законодательным предположениям своих членов и комиссий, она может 
  поручить Краевому правительству разработку соответствующего законопроекта, на 
  принятых радою основаниях. В частности, к ведению Законодательной рады относятся: 
  рассмотрение и утверждение краевого бюджета, утверждение проектов правительственных 
  займов, определение ежегодного контингента лиц, подлежащих призыву на военную 
  службу, утверждение договоров, заключаемых с другими государствами, государственными 
  образованиями и областями, и обсуждение деятельности кубанского краевого правительства, 
  к которому она может обращать запросы и вопросы. <br>
  <br>
  17. Члены Законодательной рады принимают участие в заседаниях Краевой рады с 
  правом совещательного голоса. <br>
  <br>
  18. Законодательная рада собирается ежегодно по праву на очередную сессию, открываемую 
  15-го ноября. На Чрезвычайную сессию Законодательная рада созывается войсковым 
  атаманом по его инициативе, а равно по заявлению не менее одной трети числа 
  членов Законодательной рады. <br>
  <br>
  19. Немедленно по открытию очередной сессии Законодательной рады на ее рассмотрение 
  правительством вносится роспись краевых расходов и доходов.<br>
  <br>
  20. До принятия Законодательной радой росписи никакой налог не может быть взимаем, 
  и никакой расход не может быть произведен без специального на то разрешения 
  рады. <br>
  <br>
  Примечание: Существующие налоги и сборы, а также производимые по временным штатам 
  и особым постановлениям правительства расходы сохраняются до момента утверждения 
  Законодательной радой первой росписи краевых расходов и доходов. <br>
  <br>
  21. Законодательная рада избирает краевого контролера для наблюдения за правильным 
  исполнением краевой росписи доходов и расходов и контроля над финансово-хозяйственной 
  деятельностью всех органов исполнительной власти края. Проект росписи представляется 
  в раду с замечаниями краевого контролера. <br>
  <br>
  РАЗДЕЛ VI. <br>
  Порядок ведения дел. <br>
  <br>
  22. Заседания Краевой и Законодательной рад действительны только при наличности 
  абсолютного большинства членов. <br>
  <br>
  23. Решения в Краевой и Законодательной радах принимаются абсолютным большинством 
  голосов присутствующих членов, за исключением случаев, предусмотренных настоящим 
  положением. При равенстве голосов предложение считается отвергнутым. <br>
  <br>
  24. Краевая и Законодательная рады определяют внутренний порядок своей деятельности 
  посредством наказов. <br>
  <br>
  25. Краевая и Законодательная рады избирают свой президиум в составе председателя, 
  его товарищей и секретарей, причем президиум Краевой рады избирается лишь на 
  время сессии и прекращает свои функции вместе с ее окончанием; президиум же 
  Законодательной рады избирается в начале каждой очередной сессии и сохраняет 
  свои полномочия до избрания президиума следующей очередной сессией. <br>
  <br>
  26. Краевая и Законодательная рады могут образовывать комиссии, которые пользуются 
  правом требовать устные и письменные объяснения и справки от членов правительства, 
  всех правительственных и других учреждений и должностных лиц. Комиссии могут 
  приглашать в свои заседания сведущих лиц с правом совещательного голоса. Законодательная 
  рада может образовывать комиссии, действующие непрерывно. <br>
  <br>
  27. Заседания Краевой и Законодательной рад публичны, но по распоряжению председателя 
  или по предложению, подписанному в Краевой раде одною пятнадцатою, а в Законодательной 
  раде одною десятою присутствующих членов, двери заседания закрываются, после 
  чего абсолютным большинством голосов принимается решение о публичном или непубличном 
  порядке рассмотрения того или иного вопроса. <br>
  <br>
  РАЗДЕЛ VII. <br>
  Общие положения о Краевой и Законодательной радах. <br>
  <br>
  28. Краевая и Законодательная рады неприкосновенны. Всякое лицо, посягнувшее 
  на их свободу и независимость, отвечает как за тягчайшее государственное преступление. 
  <br>
  <br>
  29. Члены Краевой и Законодательной рад за мнения, высказываемые при исполнении 
  ими своих обязанностей, не несут никакой ответственности ни в общеуголовном 
  порядке, ни в порядке частного обвинения. <br>
  <br>
  30. Члены Краевой и Законодательной рад во время сессии без разрешения соответствующего 
  представительного собрания, не могут подлежать личному задержанию или судебному 
  преследованию, за исключением случая задержания на месте преступления. <br>
  <br>
  31. По постановлению Краевой и Законодательной рад личное задержание члена той 
  или другой рады отменяется, и судебное преследование приостанавливается на все 
  время сессии. <br>
  <br>
  32. Члены Краевой и Законодательной рад тот- час после открытия первой сессии 
  дают торжественное обещание по прилагаемому при сем тексту. <br>
  <br>
  33. Члены Краевой рады получают из краевых средств суточное вознаграждение в 
  размере, определяемом самой радой. Кроме того, они пользуются правом проезда 
  за счет краевых средств перед началом и по окончании сессии от места своего 
  жительства до места заседания рады и обратно. Члены Законодательной рады получают 
  из тех же средств денежное годовое довольствие в размере, установленном Краевою 
  радою. <br>
  <br>
  34. Члены Законодательной рады не могут занимать платные должности по назначению 
  правительственной власти, а также выборные административные или полицейские 
  должности за исключением членов правительства, краевого контролера и их товарищей. 
  Лица, занимающие означенные должности, со дня уведомления их о выборе члена- 
  ми Законодательной рады должны в двухнедельный срок подать прошение об отчислении 
  от занимаемой должности. Не исполнившие сего почитаются отказавшимися от полномочий 
  депутата. <br>
  <br>
  Примечание: Члены Законодательной рады, избранные Чрезвычайной Краевой радой 
  созыва 1918 г., сохраняют за собою право по истечении срока своих полномочий 
  занять прежнее место своей службы. <br>
  <br>
  35. Члены Краевой и Законодательной рад до истечения срока своих полномочий 
  не могут быть отозваны своими избирателями и не могут быть лишены депутатских 
  полномочий иначе, как вследствие приговора суда, с коим связано лишение избирательных 
  прав. <br>
  <br>
  36. Председатель правительства, члены правительства, краевой контролер и их 
  товарищи пользуются правом свободного доступа в заседания Краевой и Законодательной 
  рад и могут обращаться с объяснениями к собранию вне очереди, как только этого 
  пожелают. В голосовании они могут принимать участие только в том случае, если 
  состоят членами данной рады. <br>
  <br>
  РАЗДЕЛ VIII. <br>
  Войсковой атаман. <br>
  <br>
  37. Кубанский Войсковой атаман почитается главою Кубанского края и Кубанского 
  казачьего войска. Ему, как главе края, подчинены все краевые военные силы. <br>
  <br>
  38. Войсковой атаман избирается Краевой радой на четыре года из числа природных 
  кубанских казаков и горцев, а также из числа приписных казаков, прослуживших 
  Кубанскому краю не менее пятнадцати лет. <br>
  <br>
  39. Войсковой атаман обладает правом помилования и смягчения наказаний по судебным 
  приговорам, но не может прекращать судебное следствие и освобождать от ответственности 
  до постановления судебного приговора. Амнистия может быть дарована только актом, 
  изданным в законодательном порядке. <br>
  <br>
  40. Войсковой атаман приглашает председателя краевого правительства и назначает 
  членов правительства из лиц, предложенных ему председателем правительства. <br>
  <br>
  41. Войсковой атаман назначает на все военные и гражданские должности, за исключением 
  случаев, законом предусмотренных. Он производит в чины и дает награды. <br>
  <br>
  42. Войсковой атаман в двухнедельный срок утверждает принятые Законодательной 
  радой законопроекты и их обнародует. Законопроекты, признанные особым постановлением 
  Законодательной рады спешными, должны быть утверждены и обнародованы в трехдневный 
  срок. Законы, издаваемые Краевой радой, опубликовываются Войсковым атаманом 
  немедленно по их принятию радой. <br>
  <br>
  43. В случае не утверждения Войсковым атаманом законопроектов Законодательной 
  рады, последние вместе с замечаниями Войскового атамана возвращаются в Законодательную 
  раду для нового рассмотрения. <br>
  <br>
  44. Не утвержденный Войсковым атаманом законопроект, будучи вторично без всяких 
  изменений одобрен Законодательной радой большинством двух третей присутствующих 
  членов, немедленно воспринимает силу закона и в трехдневный срок опубликовывается 
  Войсковым атаманом. <br>
  <br>
  45. Войсковой атаман издает указы о созыве или перерыве сессии Законодательной 
  рады по своей инициативе или по постановлению Законодательной рады; указы о 
  закрытии сессии Законодательной рады издаются им только по ее постановлению. 
  <br>
  <br>
  46. Перерыв очередной сессии Законодательной рады без ее о том постановления 
  может произойти не ранее рассмотрения ею краевой росписи доходов и расходов; 
  перерыв очередной или чрезвычайной сессии Законодательной рады без ее о том 
  постановления может происходить не более двух раз в течение одной и той же сессии 
  и не может продолжаться каждый раз свыше одного месяца. <br>
  <br>
  47. Войсковому атаману принадлежит право законодательной инициативы. <br>
  <br>
  48. Войсковой атаман может устно или письменно делать сообщения Законодательной 
  раде о важнейших событиях, обсуждение которых в раде в присутствии Войскового 
  атамана не допускается. <br>
  <br>
  49. Все акты Войскового атамана должны быть скреплены председателем краевого 
  правительства или отдельными членами правительства, по принадлежности, без каковой 
  скрепы акты Войскового атамана недействительны. <br>
  <br>
  50. Войсковой атаман, по своем избрании, приносит присягу по прилагаемой форме. 
  <br>
  <br>
  51. В случае смерти Войскового атамана до истечения срока его полномочий, его 
  функции исполняет председатель краевого правительства до избрания нового атамана 
  Краевой радой, которая в этом случае созывается председателем правительства 
  не позже одного месяца со дня смерти атамана. В случае тяжкой болезни или выезда 
  Войскового атамана за пределы Кубанского края, его обязанности временно исполняет 
  также председатель краевого правительства. <br>
  <br>
  52. Войсковой атаман ответствен только в случае нарушения им настоящего положения 
  и в случае тяжких государственных преступлений. <br>
  <br>
  53. Войсковой атаман за исполнение своих обязанностей получает вознаграждение 
  в размере, устанавливаемом Краевою радою. <br>
  <br>
  РАЗДЕЛ IX. <br>
  Кубанское краевое правительство. <br>
  <br>
  54. Исполнительную власть в Кубанском крае осуществляет Кубанское краевое правительство 
  в составе председателя и членов, составляющих Совет правительства. <br>
  <br>
  55. Совет правительства несет солидарную ответственность перед Законодательной 
  радой за общее направление своей деятельности. За личные же свои действия члены 
  правительства несут индивидуальную ответственность. <br>
  <br>
  56. Вотум недоверия, принятый в Законодательной раде простым большинством голосов, 
  влечет за собою отставку правительства. <br>
  <br>
  57. Во время отсутствия занятий Законодательной рады, в случае чрезвычайной 
  к тому необходимости, Совет правительства пользуется правом издания законов, 
  которые требуют утверждения Войскового атамана и им опубликовываются. <br>
  <br>
  58. Законы, изданные в порядке предшествуюей статьи, должны быть внесены на 
  рассмотрение Законодательной рады в течение двух недель после укрытия ближайшей 
  сессии. Законы, не внесенные в Законодательную раду в течение указанного срока, 
  теряют свою силу. <br>
  <br>
  59. Акты Войскового атамана и краевого правительства, изданные с нарушением 
  настоящего положения, почитаются недействительными. <br>
  <br>
  РАЗДЕЛ Х. <br>
  Об ответственности Войскового атамана, членов правительства и членов Законодательной 
  рады. <br>
  <br>
  60. Порядок ответственности Войскового атамана за преступления, предусмотренные 
  статьею 52 настоящего положения, а также порядок суда над членами Краевой и 
  Законодательной рад, в случае совершения ими преступлений, определяются особым 
  законом, изданным Краевой радой.<br>
  <br>
  61. За должностные преступления, совершенные членами краевого правительства, 
  Законодательная рада может возбудить против них, а равно против совета правительства 
  в целом, обвинения и предать их суду того судебного установления, коему будут 
  подсудны Войсковой атаман и члены Законодательной рады. <br>
  <br>
  РАЗДЕЛ XI. <br>
  <br>
  62. Все законы Государства Российского, в том числе все законы, изданные Временным 
  правительством, поскольку те и другие касаются кубанского края и поскольку они 
  не отменены и не изменены высшей краевой властью, сохраняют свою силу. <br>
  <br>
  ФОРМА <br>
  торжественного обещания для членов рады. <br>
  <br>
  Мы, ниже поименнованные, обещаем пред Всемогущим Богом исполнять возложенные 
  на нас обязанности членов Краевой (Законодательной) рады по крайнему нашему 
  разумению, совести и силам, все помыслы свои направляя к пользе государственной 
  и ко благу и крепости родного Кубанского края, в удостоверение чего своеручно 
  подписуемся. <br>
  <br>
  Текст присяги Войскового атамана. <br>
  <br>
  Именем Всемогущего Бога перед Святым Его Евангелием и Животворящим Крестом и 
  перед славными боевыми знаменами Кубанского казачьего войска, обещаю верно и 
  нелицеприятно служить избравшему меня своим Войсковым атаманом Кубанскому казачеству 
