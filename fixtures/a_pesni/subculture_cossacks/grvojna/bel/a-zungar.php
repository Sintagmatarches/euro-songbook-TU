        <span class="text"><strong>Санжа 
          Балыков</strong><br>
          <br>
          <span class="header"><strong>ВОСПОМИНАНИЯ О ЗЮНГАРСКОМ ПОЛКУ</strong></span> 
          <br>
          
		  <br>
          <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
          М., «Посев», 2005, стр. 45-52. Публикация В. Ж. Цветкова.<br>
          </em>
          <br>
          <br>
          Возникновение Зюнгарского Калмыцкого полка — первый случай, когда Донские 
          калмыки за свое двухвековое пребывание в звании и положении Донских 
          казаков, получили свою отдельную воинскую часть. <br>
          <br>
          Полк этот был сформирован в мае 1918 года в станице Константиновской. 
          <br>
          <br>
          Главными инициаторами создания отдельного калмыцкого полка были члены 
          Круга покойный Сарсинов и ныне живые Уланов и Алексеев. <br>
          <br>
          Полк был четырехсотенного состава и состоял преимущественно из казаков 
          Платовской, Бурульской и Граббевской станиц, бывших в степном походе. 
          <br>
          <br>
          Так как я не был в этом полку с самого начала формирования, а попал 
          в его ряды через 7-8 месяцев, то естественно, что мои воспоминания не 
          будут достаточно полными, да и многое из того, чего участником или свидетелем 
          я был, уже позабыто, а записанных дел, к сожалению, не сохранилось.<br>
          <br>
          Таким образом, что мною здесь будет приведено, не должно быть сочтено 
          за полное перечисление и оценку всей работы Зюнгарского полка. При этом 
          я ставлю себе в задачу не впадать в область излишних восхвалений, а 
          еще больше воздерживаться от воспроизведений неточно сохранившихся в 
          моей памяти фактов. <br>
          <br>
          Поэтому все, что здесь встретится, никоим образом не может быть также 
          отнесено к произвольным измышлениям. <br>
          <br>
          Ибо жизнь Зюнгарского, а также и всякого другого полка времен гражданской 
          войны так богата переживаниями, что не только преувеличивать, но даже 
          просто перечислить все случаи не может никакая память человека. <br>
          <br>
          Следовательно, я здесь даю только отдельные обрывки, некоторые случаи, 
          ясно сохранившиеся в памяти одного человека. <br>
          <br>
          Первым командиром полка был есаул Суворов. <br>
          <br>
          Полк под его командой принимал участие во всех боях с красноармейскими 
          частями по очищению Сальского округа и в дальнейших делах по железнодорожной 
          линии от станции Великокняжеская до станции Тундутово. <br>
          <br>
          В октябре месяце 1918 года полк был переброшен в Ставропольскую губернию, 
          где три месяца прошло в беспрерывных боях в районе сел: Джалга, Яшалта, 
          Киста, Киевское и др. <br>
          <br>
          Много здесь, говорят, было интересных случаев, положений и моментов, 
          но кто же их нам передаст? Многих участников уже на свете нет, многие 
          по свету белому рассеяны. С командой пополнения в 30 конных казаков 
          я прибыл в последних числах декабря 1918 г. в Зюнгарский полк во время 
          стоянки его в большом селе Киста. <br>
          <br>
          Прибыли мы к вечеру и сразу же наткнулись на трупы 9 калмыков на церковной 
          площади. Оказалось, что они вчера попали в плен к большевикам и замучены. 
          <br>
          <br>
          Их нашли на дне глубокого колодца, куда их бросили жители. <br>
          <br>
          Командиром полка оказался есаул Андрианов. <br>
          <br>
          Я был доволен, что, наконец, попал в полк, куда давно стремился. <br>
          <br>
          Однако то, что целый полк состоит из одних калмыков и что здесь, не 
          стесняясь, говорят по-своему, в походе поют калмыцкие песни и пр. казалось 
          мне утешительным явлением. <br>
          <br>
          На второй же день моего пребывания в полку было получено приказание 
          выступить в станицу Великокняжескую на погрузку для отправки на Воронежский 
          фронт, где Хоперцы, устроив очередное братание с большевиками, оголили 
          фронт. <br>
          <br>
          Перед уходом Зюнгарцам пришлось спасать свою окружную станицу от неожиданного 
          захвата красными, появившимися со стороны восточных коннозаводчиков. 
          <br>
          <br>
          Одна бригада красных была уже в двадцати верстах: не будь случайно Зюнгарцы, 
          Великокняжеская, несомненно, с налета была бы занята ими. <br>
          <br>
          Отразив без особенного труда нападение неожиданных гостей, переночевав 
          одну ночь в экономии бр[атьев] Лисицких, на другой день прибыли в Великокняжескую, 
          в ту же ночь погрузились и покатили на север. <br>
          <br>
          Есаул Андрианов перед отправкой полка был отрешен от командования полком. 
          Я лично как-либо оценить его не успел, но общий отзыв офицеров о нем 
          не был лестным. <br>
          <br>
          Временное командование полком перешло к есаулу Антонову Гр. <br>
          <br>
          Время его командования в истории Зюнгарского полка было не блестящим. 
          <br>
          <br>
          На вторые сутки мы выгрузились на станции Глубокая. Переночевав там, 
          утром выступили по направлению к хутору Гусеву. <br>
          <br>
          Донская армия на этом фронте отступала. <br>
          <br>
          К подавленному душевному настроению отступающих прибавились трескучие 
          морозы. Неспособный Антонов и в бою и в не боя терял и распылял полк. 
          Сотни зачастую отрывались от полка и шли самостоятельно. Одна или две 
          сотни совсем куда-то были откомандированы. Командир полка с каждым днем 
          терял авторитет среди офицеров. Я начал жалеть, что попал сюда. Полк, 
          куда я так охотно пришел, казался нежизненным, небоеспособным. <br>
          <br>
          К счастью, больших и серьезных боев не было по причине всеобщего отступления, 
          и мы целиком докатились до станицы Каменской. <br>
          <br>
          Через Донец переправились по льду, но на второй день после нашей переправы 
          наступила оттепель и возможность переправы красных по льду уже была 
          исключена, что значительно облегчало условия защиты переправ. <br>
          <br>
          Донская армия задалась целью задержать красных на Донце и, как известно, 
          цель была достигнута. <br>
          <br>
          Здесь, в станице Каменской, произошла реорганизация частей. <br>
          <br>
          Многие полки расформировывались, сливались и пр. <br>
          <br>
          Зюнгарский полк оказался предназначенным к расформированию. <br>
          <br>
          Зюнгарцам это сильно не понравилось, так как расформирование не вызывалось 
          особой веской причиной. <br>
          <br>
          Выборные старики, то есть делегаты от полка, походили по начальствам, 
          и в результате полк сохранили. <br>
          <br>
          Был расформирован 83-й полк, командир которого полковник Греков принял 
          на законном основании наш полк и привел с собой несколько офицеров и 
          пулеметную команду в 10 пулеметов. <br>
          <br>
          Кроме того, из других полков прибыло десятка три калмыков, и полк стал 
          внушительнее. <br>
          <br>
          Полковник Греков первым долгом принялся за численное увеличение полка 
          калмыками. Командировал в Сальский округ офицера за пополнением, вытребовал 
          из Кавказской армии одну сотню калмыков, да и из других русских полков 
          вызвал в свой полк на основании закона всех калмыков. <br>
          <br>
          Для привития дисциплины он распорядился устраивать в сотнях часы для 
          общей чистки лошадей, водопоя и кормежки. <br>
          <br>
          Заказал в Новочеркасске торбы, щетки, скребницы. <br>
          <br>
          В свободные от боев часы пообещал казакам пеший строй, а офицерам тактические 
          занятия. Заказал в Новочеркасске духовые трубы для полкового оркестра, 
          для обозных лошадей новые обозные хомуты. <br>
          <br>
          Все эти законные и понятные намерения в обстановке мирного времени, 
          просто физически невозможны и нецелесообразны были в условиях гражданской 
          войны.<br>
          <br>
          Неудивительно, что все эти затеи ни к чему не привели. <br>
          <br>
          Возможно, что он в мирной обстановке был бы хорошим командиром, но никак 
          не походил на эту роль в гражданской войне. <br>
          <br>
          Например, боем он руководил, сидя далеко в тылу полка, получая запоздалую 
          ориентировку, отдавал неуместные приказания. <br>
          <br>
          Несмотря на такой способ ведения боя, он скоро был paнен шрапнелью в 
          ногу. Не воодушевляемые личным примером командира, Зюнгарцы при нем, 
          понятно, не сделали каких-либо крупных успехов. <br>
          <br>
          По ранении Грекова полк временно принял есаул Губкин. <br>
          <br>
          Коренной Зюнгарец, будучи храбрым и справедливым, он был довольно популярен 
          в полку. <br>
          <br>
          Наступила весна 1919 г. Воспрянули духом Донцы, решительнее и упорнее 
          стали нажимы на красных. <br>
          <br>
          Зюнгарцы под командой Губкина в первых числах апреля сделали первый 
          успешный почин в Подрепнинской операции. У села Средне-Говейное Зюнгарцы 
          атаковали батальон красных, дошли до удара холодным оружием, порубили 
          и взяли в плен роту пехоты, захватили 4 пушки совсем с упряжкой и 2 
          пулемета. Батальон этот был из той группы красных, которые форсировали 
          Донец, заняли Ренную и наступлением на Лихую угрожали уже Новочеркасску. 
          <br>
          <br>
          И удачный почин Зюнгарцев был действительно важен. <br>
          <br>
          Весь батальон уничтожить не удалось, так как красные оправились, и кинувшиеся 
          дополнительно в атаку 2-ая и 3-я сотни с уроном отошли. <br>
          <br>
          Первоначальный успех был сделан 1-й и 4-й сотней. В этом бою были убиты 
          в самой гуще красных поручик Краснолуцкий и казак Шонгальдинов, Платовской 
          станицы. Особенной храбростью отличились казаки: Сербин Лонгвинов, Платовской 
          станицы, и урядник Цебеков, Граббевской станицы. Первый, далеко опередив 
          сотню, первым врубился в ряды красных, второй хорошо управлял в бою 
          своим взводом. Начальник дивизии генерал Савельев, наблюдая за атакой 
          3юнгарцев, говорят, танцевал от радости. Вся первая и четвертая сотни 
          были произведены начдивом в подхорунжие, исключая не бывших в атаке. 
          <br>
          <br>
          Принимая во внимание, что это было первой удачей после долгого периода 
          неудач, неудивительна и радость и награда начальника дивизии. И еще 
          два дня и остатки разбитых красных, потеряв все, что можно терять, нагими 
          переплыли Донец у села Липовки. В этих боях мы потеряли боевого сотника 
          Банинова, Платовской станицы, и тяжело раненым есаула Антонова Ипполита. 
          <br>
          <br>
          Ликвидировав красных на этом участке, полки пошли на стоянку по случаю 
          Пасхи по ближайшим селам. Наш полк провел эти дни в селе Верхне-Говейном. 
          <br>
          <br>
          При переходе полка из этого села на Луганский фронт, командир полка 
          Губкин, в нетрезвом состоянии, повздорив с сотником Кочетовым, насмерть 
          зарубил его. Последовал арест Губкина для предания суду. Много было 
          хождений наших стариков по штабам для выручки своего любимого командира, 
          но все было напрасно. Добились только того, что его не судили на фронте, 
          а отправили в Новочеркасск, где оправдали, но Зюгарцам обратно не дали. 
          <br>
          <br>
          Полк принял подъесаул Пупков Гавриил. Этот офицер, несмотря на свой 
          малый чин, так хорошо справлялся со своей работой, что и после этого 
          до конца войны неоднократно командовал нашим полком. У нас как-то привыкли 
          смотреть на него, как на прямого заместителя командиров полка, что даже 
          многие штаб-офицеры не считали для себя обидным быть в его подчинении. 
          Его, очень вежливого, умного и любящего калмыков, любили и калмыки. 
          <br>
          <br>
          «Ики сахалта манна кун» (Наш усатый человек), - говорили про него казаки. 
          Под его командой Зюнгарцы совместно с 78-ым полком под селом Аннинским 
          17 апреля забрали и уничтожили батальон красных. <br>
          <br>
          В этой атаке был убит исполнявший должность взводного офицера подхорунжий 
          Даржинов, Бурульский ст. и Даланта Бархаринов Ден. ст. <br>
          <br>
          В конце апреля наша дивизия сделала набег в тыл станции Глубокой, не 
          переправившись через Донец по узкому плавучему мостику в один конь. 
          При первом столкновении с красными атака 3-й и 4-й сотни Зюнгарцев (сотники 
          Ремелев Д. и Акользин) дала 4 пулемета и десяток пленных, но, благодаря 
          наступившей темноте, успех этот развить не удалось. В этой атаке были 
          убиты подхорунжий Саргинов Калмак и казак Балданов, оба Баглаевской 
          станицы. <br>
          <br>
          Убитые остались у самой цепи красных, чтобы взять их, потребовался геройский 
          поступок подхорунжего Цебекова, Граббевской станицы, вызвавшегося на 
          это дело добровольно. Темной ночью он один пешком пошел к цепи красных 
          и на себе одного за одним принес убитых. <br>
          <br>
          На другой день красные заставили нашу дивизию спешно и беспорядочно 
          отступать на переправу. Но Зюнгарцы и тут не растерялись: наш полк без 
          приказания начал прикрывать отступление, даже перейдя в контратаку, 
          отбили свои брошенные три пулемета и вывезли на быках нашу брошенную 
          пушку. Начальник дивизии прислал нам приказание «не зарываться». К вечеру 
          нажим красных ослабел, и дивизия благополучно переправилась обратно. 
          <br>
          <br>
          В этих боях командовал есаул Зипунников, заменивший заболевшего Пупкова. 
          Этот тоже коренной Зюнгарец — кадровый офицер. Скоро командиром нашего 
          полка на законном основании был назначен полковник Никулин, Великокняжеской 
          станицы, очень боевой человек. Под его командой полк сделал набег на 
          станцию Родаково. Полки дивизии, стоявшей в селе Беляны, перед рассветом 
          должны были атаковать Белянские высоты, занятые красной пехотой. <br>
          <br>
          В назначенный час полки тронулись на своих участках. <br>
          <br>
          Темнота — хоть глаз выколи. Переправились через реку и пошли. Прошли 
          довольно долго, противника нет. Идем дальше... Вдруг на нашем левом 
          фланге раздается «ура!». Мы сейчас же поворачиваемся на них и тоже на 
          ура. Раздались выстрелы. Сошлись почти вплотную, видны уже в темноте 
          силуэты. Не бегут они — и мы тоже. <br>
          <br>
          Вдруг слышим, кричат: «Стой! Стой! Зюнгары, свои! Не стреляй!» <br>
          <br>
          - Какой полк?<br>
          <br>
          - 78-й! — Оказалось, что это наш 78-й полк, один из лихих полков. Хорошо, 
          что мы не подгадили, то-то был бы смех! Распознавши друг друга, полки 
          разошлись: 78-й вправо вдоль фронта, а мы — вглубь прямо. Идем, противника 
          все нет. Уже рассвело. Смотрим, сбоку к нам едет один верховой. Сразу 
          видать — красный. Наши как-то выдали себя, и красный, приблизившись, 
          повернул и поскакал обратно к видневшейся вдалеке станции. Полк на рысях 
          туда. При нашем приближении оттуда начинают удирать обозы и немного 
          конных. Наконец, наш полк развернулся и пошел наметом. Видим, на станции 
          бронепоезд. Но полк все-таки идет. Вдруг дали по нас картечью раз, два, 
          все кругом зажужжало и запылило. К счастью, большого урона не понесли; 
          только ранили весь штаб полка. Полковник Никулин был ранен в голову. 
          Полк подался влево вдоль полотна железной дороги и занял путь отступления 
          красных. <br>
          <br>
          Так как, не имея приспособлений, мы не могли испортить железнодорожный 
          путь, то красные, бывшие на станции (полк безоружной пехоты), сели на 
          готовый состав и прошли мимо нашего носа. Остановить мы не сумели, но 
          состав этот, набитый людьми, прошел под обстрелом всего нашего полка 
          с дистанции 15-20 шагов. За составом выбежал один паровоз. Подхорунжий 
          Яманов Хабу, Платовской станицы, вылез на полотно и приготовил ручную 
          бомбу, машинист струсил и остановил паровоз. За паровозом вышел бронепоезд, 
          подхорунжий Каралкин пустил паровоз обратным ходом навстречу броневику. 
          Броневик начал уходить обратно, паровоз начал догонять. Из броневика 
          два раза ударили из пушки по паровозу, но промахнулись, а в следующую 
          минуту паровоз на полном ходу с грохотом ударил по броневику, и он стал. 
          <br>
          <br>
          Нам не удалось воспользоваться плодами этого набега, так как им подоспела 
          с позиции помощь, не доступная нашим силам, и мы ушли восвояси. По ранении 
          Никулина полк принял и руководил боем опять Пупков Гавриил. Скоро здоровье 
          Пупкова опять расстроилось, и он уехал в обоз, сдав полк есаулу Зипунникову. 
          Полк под его командой в составе группы Секретева пошел на прорыв к восставшим 
          Верхнедонцам. Будучи в командировке, я в этом походе не участвовал. 
          По показаниям бывших, Зюнгарцы в нем нигде не осрамились. В одном бою 
          был тяжело ранен подхорунжий Яманов, Платовской станицы, известный в 
          своей 3-ей сотне храбростью. <br>
          <br>
          Выручив Верхнедонцев, Зюнгарцы в составе частей Мамантова пошли в памятный 
          прорыв — рейд. В одном из первых боев до прорыва был ранен командующий 
          полком Зипунников. Полк принял как раз к тому времени вернувшийся Греков. 
          <br>
          <br>
          В одном из боев, кажется, под Александровским поселком, генерал Мамантов 
          нашел его сидящим без полка под навесом, выгнал его оттуда и потащил 
          с собой в бой. Не успел Греков присоединиться к полку, как был ранен. 
          От этой раны он умер в Новочеркасске. <br>
          <br>
          В этом же бою был убит пулеметчик хорунжий Сапсуков. Полк принял полковник 
          Оружейников, еще молодой человек. На второй день своего прибытия в первом 
          же бою был ранен. <br>
          <br>
          Для командования Зюнгарцами прислали полковника Бочарова, который и 
          прокомандовал нашим полком весь рейд. Это был тихий, смирный и рассудительный 
          человек. К концу рейда он был назначен начальником штаба бригады, а 
          полк принял прикомандированный есаул Забазнов. По выходе полков из рейда 
          все, кто могли, тем или иным путем уехали по домам отвозить добычу. 
          Уехал и есаул Забазнов. Не оставалось ни одного офицера, которому можно 
          было бы со спокойной совестью доверить полк. <br>
          <br>
          Полком стал командовать подъесаул, а на самом деле хорунжий, по присланным 
          из 61-го полка документам, Бохольдинов Пишван, Ново-Алексеевской станицы. 
          Дальше пошло еще хуже — Бохольдинову пришлось три дня командовать бригадой, 
          хотя в бригаде этой было не больше двух сотен. <br>
          <br>
          А Буденный тем временем давил. Фронт катился. <br>
          <br>
          В Мамантовском рейде я не был, а потому работу Зюнгарцев в нем подробно 
          не знаю. Одно знаю, что Зюнгарцами Мамантов был доволен. При обратном 
          прорыве у Коротояка Мамантов ездил к генералу Шкуро и в качестве эскорта 
          взял с собой Зюнгарцев, которых представил Шкуро, как боевой полк. <br>
          <br>
          Скоро командиром полка был назначен на законном основании полковник 
          Сергеев Владимир. Вспыльчивый и грубый человек, но все же добрый, к 
          калмыкам расположенный, в мирной жизни имевший много табунщиков-калмыков, 
          о которых тепло вспоминал. Хорошо командовал и полком. Под его командой 
          полк неоднократно имел удачные столкновения с врагами. В день моего 
          прибытия 2 ноября в полк из обоза у Сергеева был убит адъютант подъесаул 
          Попов Вениамин, Аксайской станицы, бывший в Германскую войну адъютантом 
          у нашего полковника Мангатова Батыря. Сергеев заместил его мною. На 
          этой должности я и остался беспрерывно до конца гражданской войны. <br>
          <br>
          Зюнгарский полк, как и вообще полки после рейда, имел жалкий вид. Это 
          были полки только по названию. В них было не больше, чем по 50-60 шашек. 
          Помню, по моему первому подсчету, в Зюнгарском полку было 67 шашек. 
          <br>
          <br>
          Армия отступала. Всеобщий упадок духа, морозы, неудачные бои — все заставляло 
          фронт неудержимо катиться на юг. Наконец, вернулся долгожданный Мамонтов. 
          Дав безрезультатный, целодневный бой своим корпусом под Сватовым, он, 
          вероятно, увидел, что произошло с его знаменитым корпусом. В этом бою 
          у нас были ранены хорунжий М. Буджалов, командующий 4-ой сотней и хорунжий 
          Ермилов А.С. <br>
          <br>
          После этого Мамантов поскакал в тыл, произвел там чистку, разгромил 
          обозы, оставив в них по три подводы, строго определили обозные штаты, 
          и полки снова пополнились. Не знаю, откуда он их вытянул, но помню, 
          наш полк получил больше 120 человек конного пополнения. Стало лучше. 
          После Сватова наша дивизия имела серьезные и удачные бои под Бровальским 
          конным заводом и под хутором Родионовской недалеко от Ростова. В этих 
          боях Зюнгарцы участвовали под командой Сергеева. Так как в обоих случаях 
          дело было с пехотой, то бои кончились пленением противника. В бою под 
          Провальским Зюнгарцы довольно долго выдержали жестокий артиллерийский 
          огонь и под конец совместной с 78-м и 96-м полками атакой кончили дело. 
          <br>
          <br>
          Здесь были ранены подъесаул Зотов, командовавший 4-ой сотней, и вахмистр 
          Кузьма Сасыков. В бою под Родионовкой красные, сбитые с позиции и окруженные 
          нашей бригадой (78-ой и Зюнгар), построили каре и ощетинились. Пушек 
          и пулеметов близко не было. Голыми руками не возьмешь. Оставить их и 
          дать возможность уйти — преступление. Крики «ура!» с их и с нашей стороны 
          не умолкали, щелкали беспорядочные винтовочные выстрелы, уже там и сям 
          были раненые. <br>
          <br>
          Вдруг из рядов нашего полка с дикими криками вырвался кто-то маленький, 
          в желтой шубе и, подлетев к красным, начал налево и направо рубить. 
          «Ура!» раздал сильнее, и наши два полка с остервенением набросились 
          на красных, и в следующие 3-4 минуты красная бригада сдалась. <br>
          <br>
          Этим первым, кинувшимся и давшим толчок остальным, был хорунжий Сельнинов 
          Иван, Платовской станицы. <br>
          <br>
          Под командой же Сергеева; полк принимал участие в удачном бою с конницей 
          Буденного в средних числах января под станицей Ольгинской. Успех был 
          настолько серьезен, что будь в это время Мамантов, от знаменитой Буденновской 
          конармии могло бы ничего не остаться. <br>
          <br>
          Но Донской конницей командовал, за отсутствием Мамантова, Павлов, «старая 
          шляпа», по отзыву всех офицеров: и успех, достигнутый в результате кровавых 
          боев не был умело до конца использован. В этот период времени имели 
          еще два-три удачных столкновения частичного характера, но в общем весь 
          фронт, задержавшись на Маныче, по флангам осаживал. В полках начал свирепствовать 
          тиф. В первых числах января в наш полк влили две сотни Сальского полка, 
          около 200 конных казаков-калмыков. <br>
          <br>
          Это дало нам возможность сформировать еще две сотни: 5-ую и 6-ую. Командирами 
          новых сотен были назначены подъесаул Чеботарев и сотник Ремилев Д. Оба 
          были известны, как боевые офицеры и до этого неоднократно командовавшие 
          сотнями. <br>
          <br>
          Таким образом, наш полк в январе 1920 года стал 6-ти сотенного состава. 
          В этом же месяце во время стоянки полка в селе Проциковом, полковник 
          Сергеев за нелады с комбригом был отрешен от командования полком, и 
          Зюнгарцев, к их радости, принял полковник Тепкин, который и оправдал 
          надежду полка. <br>
          <br>
          При Тепкине мы участвовали памятном по жертвам походе по безлюдной Заманыческой 
          степи на станцию Торговую. <br>
          <br>
          Несмотря на голод и холод, цель набега была блестяще достигнута. <br>
          <br>
          Конница Буденного, захваченная ночью врасплох, бросив все, пешком бежала 
          до следующей назад станции — Шаблиевки. Но фатальный Павлов опять послужил 
          причиной небывалого несчастия для Донской конницы. <br>
          <br>
          Говорили, что он, будучи во главе 2-го корпуса, на крайнем правом фланге 
          нашем, наткнулся на забытый красными пост в 6-7 человек на мосту на 
          окраине Воронцовки. К чести этих красноармейцев, они, когда подошел 
          Павлов с корпусом, не струсили, а громко окликнув: «Кто идет?», - открыли 
          огонь. <br>
          <br>
          Павлов замялся, между тем двух-трех ранили, передние стали поворачивать 
          лошадей, задние, не зная в чем дело, тоже. И вдруг корпус дрогнул и 
          обратился в дикое паническое бегство. Паника, как пожар, перекинулась 
          на другие корпуса, и все загрохотало под копытами десятка тысяч лошадей. 
          Пришлось бежать и частям, занявшим Воронцовку. Красные, недоумевая, 
          вернулись только к утру. <br>
          <br>
          Донская конница заночевала в чистом поле при 20-градусном морозе. Люди 
          уже вторые-третьи сутки полуголодные начали насмерть замерзать, кто 
          где лег. В результате части без боя теряют 50% своего состава. В этом 
          несчастном походе Зюнгарцы показали далеко превосходящую остальных выносливость. 
          Тогда как в других частях процент вышедших из строя достигал указанных 
          процентов, и много было совсем замерзших, из Зюнгарского полка вышло 
          с обмороженными частями тела только 47 человек, из них 10 русских офицеров. 
          Замерзших насмерть у нас не было. <br>
          <br>
          Правда, мы всю ночь не ложились и танцевали, ели и сырое мясо, но зато 
          «победили природу». <br>
          <br>
          Под командою же Тепкина мы были в неудачном для нас бою под Песчаноокопской. 
          На другую ночь мы сделали удачный набег на хутор Г. Были также в последнем 
          решительном бою под станцией Егорлыцкой. В этом бою был ранен командир 
          полка полковник Тепкин. Полк принял опять подъесаул Пупков Гавриил. 
          <br>
          <br>
          В дальнейшем отступлении мы, случайно встретив, прикрывали отступление 
          нашей 6-ой пехотной дивизии, за что получили благодарность комкора Секретева. 
          <br>
          <br>
          Дальше в отступлении по Кубани интересных, а, главное, успешных, для 
          нас боев не было. Каждая стычка, каждый бой кончались для нас, в конце 
          концов, отступлением. <br>
          <br>
          О Зюнгарцах в бою под станицей Динской, когда 1-ая сотня по моей частной 
          инициативе прикрывала беспорядочную давку на гати, упоминает Г. Раковский 
          в своей книге «В стане белых». Между прочим, восклицает: «Только они 
          еще были крепки, только они спасали положение» и т.д. Видел и я, помню, 
          как мимо моего правого фланга проскакал на взмыленной лошади Командарм 
          Сидорин с группой всадников-юнкеров, наверное, среди них и был Раковский. 
          <br>
          <br>
          О другом случае, когда Зюнгарцы перед атакующими красными на руках протащили 
          до дороги загрузший в грязи аэроплан Командарма Сидорина, то же пишет 
          тот же Раковский в той же своей книге. Это спасение командарма стоило 
          нам жизни 2 казаков, не успевших переправиться и попавших в плен. Многим 
          пришлось бросить лошадей и, перебегая по льду, провалиться и искупаться 
          в холодной воде. Между прочим, я видел и генерала Постовского, тоже 
          искупавшегося. <br>
          <br>
          Отступление продолжалось и принимало безнадежный характер. В станице 
          Ново-Дмитриевской присоединился к полку, еще с пулей в шее, Тепкин и 
          принял полк. В это время по пути отступления умер командир 4-ой сотни 
          Трудников Е.А., коренной Зюнгарец, один из лучших офицеров полка. Сотню 
          его принял подъесаул Ремелев Саран, прокомандовавший ею до конца войны. 
          <br>
          <br>
          При дальнейшем отступлении Зюнгарам пришлось увидеть остающихся по невозможности 
          дальше следовать своих беженцев-калмыков. Происходили душераздирающие 
          сцены расставания на неизвестное время, перед захватом беспощадного 
          врага, матерей с сыновьями, жен с мужьями и т.д. Тяжесть этой картины 
          может понять только тот, кто пережил, или кто непосредственно видел. 
          Редко кто не плакал, проходя мимо оставляемых на произвол судьбы без 
          надежды когда-нибудь увидеться наших родных и знакомых, женщин, детей 
          и стариков. <br>
          <br>
          Вступив в горы, до прибытия в Крым обошлись без хлеба. Ели мамалыгу. 
          Труден был этот переход. Некованые кони подбивались и падали, остро 
          ощущался и недостаток корма. <br>
          <br>
