          сотни Буджалов свободно переругивается с комиссаром. Так как красных 
          больше, а сзади их видно еще, то наша сотня уже, вижу, мнется. Мой приезд 
          их немного ободрил, потому что, если я здесь, значит и полк не далек. 
          С казаком на хорошей лошади я сообщил командиру в чем дело и просил 
          скорее полк. <br>
          <br>
          В это время перед нами разворачивается еще лава — уже целый полк. А, 
          там влево, вправо еще два полка, мы уже в мешке — стало жутко. Прошло 
          еще 5-6 томительных минут. Наконец, запылил и наш полк и на намете начал 
          разворачиваться и вливаться в лаву, 6-ая сотня правее, остальные левее 
          5-ой сотни. Весь полк втянулся в бой. Красные развернули еще два полка, 
          и опять наш полк оказался по внутренней линии подковообразного расположения. 
          Расположение противных лав так близко, что капитан Сенкевич первый снаряд 
          положил на нашу 4-ую сотню и в клочки разнес хорунжего Ширшикова и оторвал 
          голову лошади хорунжего Церенова. Но зато уже дальше начал так бить 
          по красным, что буквально ни один выстрел не пропадал даром... <br>
          <br>
          Зюнгарцы здесь при всем желании не могли кинуться в атаку, слишком явно 
          было пятерное превосходство противника. Тем более, опасно было пробовать 
          отступление, так как не было сомнения, что оно превратится в бегство, 
          и половина полка не выйдет из мешка. Единственным выходом было продержаться 
          до подхода 18-го полка. И Зюнгарцы, мужественно защищаясь, продержались. 
          <br>
          <br>
          Хорошо помогала наша пушка, бившая прямой наводкой по ясно видимой цели, 
          но много потребовалось и от нашего полка. Несчастье красных состояло 
          в том, что они почему-то не кинулись на нас, что должны были сделать, 
          а, подойдя почти вплотную, продолжали перестреливаться. <br>
          <br>
          В этом бою произошли два интересных и красивых случая. <br>
          <br>
          Против нашей 4-ой сотни из рядов красных выскочил один лихач и, кроя 
          нас звучными ругательствами, начал вызывать на бой. Навстречу ему вылетел 
          командовавший 4-ой сотней в этом бою сотник Сельнинов. Как в какой-нибудь 
          древней Куликовской битве, сшиблись они при общих криках ура и... миг, 
          лошадь красного понеслась к своим без всадника, который остался лежать 
          с раскроенным черепом. <br>
          <br>
          Против первой сотни выскочил вперед пулемет на тачанке и хотел в упор 
          открыть огонь. Навстречу к нему бросились Ш. Баранов и Х. Бохардинов. 
          Не успела тачанка открыть огонь, как наши зарубили пулеметчика и умчали 
          пулемет из-под носа красных. <br>
          <br>
          Так прошло 10-15 минут. Наконец, наши Георгиевцы во взводной колонне 
          с шумом и гиком ворвались в нашу дугу и прорвали красную лаву. Бросились 
          и Зюнгарцы, красные бежали, преследуемые нами до самой Поповки. После 
          этого мы вернулись на свой прежний участок, а красные пошли дальше вдоль 
          нашего фронта и на участке 1-ой Дондивизии прорвались в наш тыл, где 
          в Токмаке чуть не захватили нашего Донатамана со знаменем для нашего 
          полка. В этом бою был ранен командир 1-ой сотни есаул Шарапов, подхорунжий 
          Кутушов Б. 2-ой сотни. <br>
          <br>
          Вскоре после этого, памятного Зюнгарцам, боя, наша бригада имела столкновение 
          с еще одним серьезным противником, с матросами, под селом Бельманкой. 
          Красные занимали позицию по северо-западной окраине села. Постреляв 
          немного из пушки, полки наши по обыкновению сели и пошли в атаку. Противник 
          был сбит с позиции, но, заняв огороды и канавы дворов, отчаянно защищался. 
          Полки наши, обнаглевшие в частых пленениях пехоты, лезли. Когда мы начали 
          окружать деревню, а пушки бить на картечь, матросы дрогнули и были взяты. 
          Ликвидировать всех все же не удалось, часть, благодаря своей твердости, 
          ушла, сбив нашу недавно прибывшую в полк, а потому еще не бывшую в боях 
          3-ю сотню (сотник Куберлинов). По упорству сопротивления это был один 
          из редких боев. В этом бою погиб хороший подхорунжий Дундуков первой 
          сотни. <br>
          <br>
          Перед самым отступлением наша 4-ая сотня в селе Романовском врасплох 
          была захвачена красными и едва унесла ноги. Сотня в этом бою потеряла 
          вахмистра Джеджинова, Власовской станицы. Также в последних боях был 
          ранен урядник Бадьма Манжиков, Денисовской станицы, 5-ой сотни. <br>
          <br>
          Вследствие неудач на Каховском направлении началось отступление фронта 
          в Крым. Для наших полков отступление прошло без больших давлений со 
          стороны противника. Каково же было наше удивление, когда мы узнали, 
          что пути отступления уже заняты красными. <br>
          <br>
          И действительно, при нашем подходе станция Сальково оказалась занятой 
          дивизией красной конницы. Недолго думая, мы двумя полками накинулись 
          на них и заставили их с уроном очистить нам путь. Оказалось, что отрезанных 
          частей было много, а еще больше тыловых учреждений, санитарных поездов 
          и проч. Таким образом, выражаясь тогдашним языком: «Мы с треском вышибли 
          Сальковскую пробку». <br>
          <br>
          Трудны были условия сидения по морозу на Татарском валу, на Сиваше у 
          Тюп-Джанкоя, но, пока сидела наша бригада, попытки красных перейти Сиваш 
          кончались неудачей. Здесь у Зюнгарцев был тяжело ранен храбрый казак 
          Оля Очуров, Денисовской станицы, и умер в Константинополе. <br>
          <br>
          Около 50 вражьих трупов лежало у нашей проволоки. Не успели мы уйти 
          оттуда, сдав участок какому-то немецкому полку, как красные в ту же 
          ночь перешли Сиваш. Прошло еще два дня, и мы с грустной мыслью о неизвестностях 
          будущего шли на погрузку в Керчь.<br>
          <br>
          Сила ломала солому. <br>
          <br>
          Зюнгарцы до самой Керчи были в арьергарде. Погрузка и эвакуация, как 
          известно, прошли благополучно. Только у нас разбежались по пути наши 
          музыканты с трубами, которые в бою под Александровском были отбиты нами 
          у красных. <br>
          <br>
          Борьба была кончена. Дальше... Константинополь, Чилингир, Кабакджа, 
          английский обоз, возвращение и расселение по разным странам. Кто их 
          не пережил, кому они не памятны и кому не тяжело их вспоминать... <br>
          <br>
          В заключение хочу сказать, что я не утверждаю, что Зюнгарский полк был 
          самый боевой полк. Нет, были полки не ниже нашего по боеспособности, 
          а некоторые и выше. Если сказать, что Зюнгарский полк был один из боевых 
          полков в Донской армии, это будет правда.<br>
          <br>
          Кроме того, наш полк имел два не присущих другим частям качества. <br>
          <br>
          Это безусловная надежность в смысле верности своему долгу и несравненная 
          выносливость в перенесении холода, голода и других лишений. Одно то, 
          что наш полк единственный в Донской Apмии, который перенес свою долю 
          бремени на всем протяжении гражданской войны без каких бы то ни было 
          расформирований и переформирований, почти при неизменном составе и одном 
          названии, должно говорить многое. <br>
          <br>
          Царство небесное вам, мои павшие однополчане! Здоровье и крепость живым!<br>
          <br>
          <em>1927 г., Чехия <br>
          <br>
          Улан Залат, 1 мая 1927 г. Воинский архив, 1970 г., № 1320(а)</em><br>
