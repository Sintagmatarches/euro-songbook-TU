<br>
  <br>
        Антибольшевистская оренбургская казачья версия советской 
        песни <a href="../oficial/orlenok.php">&quot;Орленок&quot;</a> (музыка 
        Виктора Белого, слова Якова Шведова, 1937, там же см. ноты).  На &quot;Малой энциклопедии русской военной песни&quot; есть <a href="http://files.pobeda.ru/music/entsiklopedia/iskazhennie/orlenok_1.mp3">mp3</a> 
        этого  варианта и <a href="http://files.pobeda.ru/music/entsiklopedia/iskazhennie/orlenok_2.mp3">mp3</a> 
        обычного &quot;Орленка&quot;.
  Этот же вариант как сомнительный опубликован в сб.: Антология военной песни 
  / Сост. и автор предисл. В. Калугин. - М.: Эксмо, 2006.<br>
  <br>
  Никаких доказательств реального бытования 
        этой песни в годы Гражданской войны нет (история появления советской песни, напротив, четко установлена), однако появилась она, возможно, не в 1980-90-е, когда пришла массовая антибольшевистская мода, а раньше, причем именно в оренбургской казачьей среде. <br>
  <br>
  Пишет историк Павел Шехтман &lt;sfrandzi @ yandex.ru&gt; 1 июля 2012 года: <br>
  <br>
  <em>&quot;В лагере в Химкинском лесу я познакомился с весьма интересным человеком, потомственным уральским казаком, который сообщил мне полностью и в отрывках песни уральских казаков времен Гражданской войны.  Несколько слов об информанте. Сергей Васильевич Свойкин  1966 г. р. Потомственный оренбургский казак,  в казачьих кругах известен как «Серега-есаул».  Родился в Уфе. Внук колчаковца (дед его командовал эскадроном, точно подразделения не помнит, но помнит, что в составе Оренбургской, затем Южной армии).  Окончил истфак Ленинградского университета. Начинал как анархист (анархистом стал в 13 лет, еще в глубоко советское время), и в эпоху перестройки был в рядах крайне левой тусовки. Затем участвовал в Приднестровском и Абхазском конфликтах и обороне Белого дома в 1993 г., в Абхазии же и получил чин есаула.  После 1993 г. работал с националистами, считая, что они более боевые и скорее смогут свергнуть «антинародный режим»; при этом, по его словам, и там не скрывал своих левых убеждений. В Химкинском лесу появился вместе с троцкистами, по взглядам считает себя народником и наследником эсеров.  В настоящее время он без работы, т.к. организовал профсоюз охранников в Уфе, после чего подвергся преследованиям и был уволен. Это я к тому, что человек по своим взглядам на Гражданскую войну старается быть максимально трезвым и историчным и дистанцироваться как от «белой», так и от «красной» мифологии, и я не вижу оснований подозревать его в мифотворчестве.
Итак, по его словам,<br>
<br>
1)        В его детстве старики (среди которых еще было немало колчаковских ветеранов) пели песню «Орленок», про судьбу пленного казака, из которой он запомнил незначительные  отрывки:
  <br>
  <br>
В разведку я послан своим атаманом…<br>
...с  врагами сражались в боях под Челябой родной…<br>
…в чекистских застенках…<br>
…кололи меня большевистским штыком...<br>
Лети на станицу, родимой расскажешь, как сына вели на расстрел&quot;.<br>
   </em><br>
  Если воспоминания Сергея Свойкина точны, то антибольшевистская песня бытовала по меньшей мере в 1970-е годы. Кстати, в ней связь между героем и орленком выражена более последовательно, чем в версии Шведова, детали реалистичней. Шведов не участвовал в Гражданской войне (родился в 1905 году, всю Гражданскую войну проработал на заводе в Москве), а автор антибольшевистской версии, возможно, участвовал. Сами орлы привычны и для уральского пейзажа оренбургских казаков, и для бурятского (в тексте Шведова действие происходит в Бурятии).<br>

