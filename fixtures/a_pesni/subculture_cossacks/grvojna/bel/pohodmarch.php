Не дубравушка шумит —<br>
Волчья сотня Шкуро едет,<br>
Мать-земля под ней дрожит.<br>
<br>
Трубите же, «волки», свой клич боевой,<br>
Вся сотня в поход выступает,<br>
Всегда впереди на коне наш герой,<br>
Душа его страха не знает.<br>
<br>
То не гром на небе ясном,<br>
То не молния блестит —<br>
В огневом бою опасном<br>
От Шкуро приказ летит:<br>
<br>
«Вперед же, герои, вперед все за мной!<br>
День правды святой наступает;<br>
Пробил для насильников час роковой,<br>
Плеть наша по гадам гуляет».<br>
<br>
То не месяц светит ясный,<br>
То не звездочка блестит, —<br>
То народный стон ужасный<br>
Грудь казацкую щемит.<br>
<br>
Гей вы, казаки, за правду борцы!<br>
Пощады врагу не давайте,<br>
Скорей же спешите в Москву, удальцы,<br>
Живьем комиссаров поймайте.<br>
<br>
То не сокол-кречет ясный<br>
По поднебесью летит, —<br>
То Шкуро, всем красным страшный,<br>
«Волкам» слово говорит:<br>
<br>
«Покончивши дело победой, бойцы,<br>
Вы честью его закрепите:<br>
Корнилова знамя, «волки»-удальцы,<br>
На стенах Кремля водрузите!» <br>
