  Там вдали за рекой засверкали огни<br>
В небе ясном заря догорала,<br>
Сотня казаков из дутовских войск<br>
На разведку в Тургай поскакала<br>
<br>
Они ехали молча в ночной тишине<br>
По уральской  по выжженной степи<br>
Вдруг вдали у реки засверкали штыки –<br>
Это краснобандитские цепи<br>
<br>
Помолившись, отряд поскакал на врага<br>
Завязалась кровавая битва,<br>
И казак молодой вдруг поник головой –<br>
Оренбургское сердце пробито<br>
<br>
Он упал возле ног вороного коня<br>
И закрыл свои ясные очи –<br>
Ты конек вороной, передай дорогой<br>
Что я честно погиб за Россию. <br>
  <br>
  <em>Прислал историк Павел Шехтман &lt;sfrandzi @ yandex.ru&gt; 1 июля 2012 года. Записана в июне 2012 года в лагере в Химкинском лесу от Сергея Васильевича Свойкина 1966 г.р., потомка оренбургских казаков. По словам Свойкина, текст он взял из газеты &quot;Оренбургские ведомости&quot; за 1919 г., и ксерокопия есть у него дома в Уфе. Вместе с этой песней записаны <a href="potiandymom.php">&quot;Потянуло дымом от Урал-реки&quot;</a> и фрагмент антибольшевистского <a href="orlenok-bel.php">&quot;Орленка&quot;</a>.</em><br>
  <br>
  <br>
Об информанте Павел Шехтман пишет следующее:<br>
<br>
<em>&quot;В лагере в Химкинском лесу я познакомился с весьма интересным человеком, потомственным уральским казаком, который сообщил мне полностью и в отрывках песни уральских казаков времен Гражданской войны. Несколько слов об информанте. Сергей Васильевич Свойкин 1966 г. р. Потомственный оренбургский казак, в казачьих кругах известен как «Серега-есаул». Родился в Уфе. Внук колчаковца (дед его командовал эскадроном, точно подразделения не помнит, но помнит, что в составе Оренбургской, затем Южной армии). Окончил истфак Ленинградского университета. Начинал как анархист (анархистом стал в 13 лет, еще в глубоко советское время), и в эпоху перестройки был в рядах крайне левой тусовки. Затем участвовал в Приднестровском и Абхазском конфликтах и обороне Белого дома в 1993 г., в Абхазии же и получил чин есаула. После 1993 г. работал с националистами, считая, что они более боевые и скорее смогут свергнуть «антинародный режим»; при этом, по его словам, и там не скрывал своих левых убеждений. В Химкинском лесу появился вместе с троцкистами, по взглядам считает себя народником и наследником эсеров. В настоящее время он без работы, т.к. организовал профсоюз охранников в Уфе, после чего подвергся преследованиям и был уволен. Это я к тому, что человек по своим взглядам на Гражданскую войну старается быть максимально трезвым и историчным и дистанцироваться как от «белой», так и от «красной» мифологии, и я не вижу оснований подозревать его в мифотворчестве&quot;.</em><br>
<br>
<br> 
Это &quot;дутовский&quot; вариант красноармейской песни <a href="../narod/tamvdali.php">&quot;Там вдали, за рекой&quot;</a> на слова Николая Кооля, написанной в 1924 году. Ритм и рифма явно хромают. Пока не предъявлен оригинал - исходная газета, - поверить в подлинность трудно, хотя подлинность этой песни все бы расставила на свои места. Тогда бы хронология выглядела так:<br>
<br>
1905 - уральскими, забайкальскими или донскими казаками сочинена песня русско-японской войны <a href="../../kazaki/za_laohe.php">&quot;За рекой Ляохэ загорались огни&quot;</a> (на мотив <a href="../../popular20/kogda_na.htm">&quot;Лишь только в Сибири займется заря&quot;</a> - возможно, восходящий к <a href="../../romans/andaluz.php">&quot;Андалузянке&quot;</a>).<br>
<br>
1918-1920 - появляются ее белогвардейские казачьи версии, к которым относится и &quot;дутовская&quot;. <br>
<br>
1924 - Николай Кооль переделывает белую песню в красную, что не являлось редкостью (так же были созданы <a href="../narod/nachparovoz.php">&quot;Наш паровоз&quot;</a>, 1922, и пионерская <a href="../../baby/kartochka-p.php">&quot;Картошка&quot;</a>).<br>
