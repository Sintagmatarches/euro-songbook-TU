        В деревушке, в убогой избушке <br>
        Жили мирно два сына с отцом. <br>
        Уважали их все в деревушке <br>
        И считали хозяйственным дом. <br>
        <br>
        Революция огненным пылом <br>
        Пронеслася над миром грозой. <br>
        За свободу, за волю народа <br>
        Кровь горячая льется рекой. <br>
        <br>
        Привезли пулеметы и пушки. <br>
        Стон, проклятья, расстрелы и крик. <br>
        И спалили огнем деревушку, <br>
        И заплакал несчастный старик. <br>
        <br>
        Старший сын в одну темную ночку <br>
        В Красну Армию тайно ушел. <br>
        Дали младшему в руки-винтовку, <br>
        Добровольцем он к белым пошел. <br>
        <br>
        Но судьба ль так жестока над нами: <br>
        И в бою, на пожарах войны, <br>
        Повстречались два брата врагами <br>
        И друг друга узнали они. <br>
        <br>
        Старший молвил: «Ты в белых солдатом, <br>
        Твой девиз — и грабеж и расстрел. <br>
        Не зову тебя больше я братом», — <br>
        И винтовку он взял на прицел. <br>
        <br>
        На курок нажимал он бесцельно, <br>
        Младший брат за винтовку схватил, <br>
        Совершилось ужасное дело — <br>
        Штык он старшему в сердце вонзил. <br>
        <br>
        И стоял он, как Каин-преступник, <br>
        Старший брат перед ним умирал. <br>
        Младший брат, от свободы отступник, <br>
        Горько-горько над трупом рыдал: <br>
        <br>
        «Тебе, братец мой, вечная память, <br>
        Все друзья на могилу придут, <br>
        Принесут в руках красное знамя, <br>
        Честь и славу тебе воздадут. <br>
        <br>
        Эх, а как же домой я явлюся? <br>
        Все родные меня проклянут, <br>
        Что не есть я защитник свободы. <br>
        И бандитом меня назовут». <br>
        <br>
        И над трупом склонилися лозы, <br>
        Вышло солнце сраженье смотреть. <br>
        Но не лей ты горячие слезы, <br>
        Труп холодный тебе не согреть. <br>
