        1. Как в одной небогатой деревне...</strong><br>
        <br>
        Как в одной небогатой деревне<br>
        Жили мирно два брата с отцом.<br>
        Ни о чем они там не тужили,<br>
        И текла иха жизнь чередом. <br>
        <br>
        Революция огненным пламенем<br>
        Пронеслася над миром грозой.<br>
        За свободу и волю народа<br>
        Кровь лилася горячей струёй. <br>
        <br>
        Привезли пулеметы и пушки,<br>
        Тут проклятья, расстрелы и крик...<br>
        Запалили огнем деревушку,<br>
        И заплакал несчастный старик. <br>
        <br>
        Отобрали коровку, лошадку,<br>
        В Красну Армию старший ушел.<br>
        В руки младшему дали винтовку,<br>
        Добровольцем он к белым пошел. <br>
        <br>
        И судьба их свела, как нарочно,<br>
        Повстречались в бою первый раз.<br>
        Друг на друга так долго смотрели,<br>
        Не могли оторвать своих глаз. <br>
        <br>
        Старший молвил: «Ты белым солдатом,<br>
        Твой девиз грабежи и расстрел.<br>
        Не зову я тебя больше братом...»<br>
        И винтовку он взял на прицел. <br>
        <br>
        Он курок нажимал так несмело,<br>
        Младший брат тут винтовку схватил —<br>
        И свершилось кровавое дело:<br>
        Штык он старшему в сердце вонзил. <br>
        <br>
        И стоит, точно Каин-преступник,<br>
        Старший брат перед ним умирал,<br>
        Точно ирод, свободы отступник,<br>
        Он над трупом ребенком рыдал. <br>
        <br>
        А над трупом склонилися лозы,<br>
        Вышло солнце сраженье смотреть...<br>
        Ты не лей же горячие слезы,<br>
        Труп холодный теперь не согреть. <br>
        <br>
        <em>Багизбаева М. М. Фольклор семиреченских казаков. Часть 2. Алма-Ата: Мектеп, 1979.</em><br>
