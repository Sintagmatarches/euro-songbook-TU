        <strong>2. В деревенской крестьянской избушке... </strong><br>
        <br>
        В деревенской крестьянской избушке <br>
        Жили дружно два брата с отцом, <br>
        Уважали все в деревушке, <br>
        И дела у них шли чередом. <br>
        Революция огненным пламенем <br>
        Пронеслася над миром грозой, <br>
        И за правое дело народа <br>
        Ала кровь проливалась рекой. <br>
        Отобрали лошадок, коровку, <br>
        Старший брат в ряды красны пошел, <br>
        Дали младшему в руки винтовку, <br>
        Добровольцем он к белым пошел. <br>
        Но судьба так смеется над нами: <br>
        Бой идет, от пожара — огонь, <br>
        Вот сошлись два брата врагами, <br>
        И узнали друг друга они. <br>
        Старший молвит: «Ты белым солдатом, <br>
        Ваш девиз есть насилие и расстрел, <br>
        Не зову, не зову тебя больше братом». <br>
        И винтовку он взял на прицел. <br>
        На курок нажимал он несмело, <br>
        Младший брат вдруг винтовку схватил, <br>
        Совершилось позорное дело, <br>
        Штык он старшему в сердце вонзил. <br>
        Старший брат, пораженный ударом, <br>
        Окровавленным трупом упал, <br>
        Младший брат, точно каин-преступник,<br>
        Над остывшимся трупом рыдал. <br>
        Но судьба так смеется над нами, <br>
        Луна вышла на битву смотреть. <br>
        — Ты не лей, брат, горючие слезы, <br>
        Труп холодный тебе не согреть. <br>
        И кругом уже все замолчало, <br>
        Не поет уж теперь соловей, <br>
        А старик, бедный умер на печке, <br>
        Не дождавшись с войны сыновей. <br>
        <br>
        <em>Багизбаева М. М. Фольклор семиреченских казаков. Часть 2. Алма-Ата: 
        Мектеп, 1979, № 246. Записана от Лебеденковой А. Т., 1917 г. р., г. 
        Иссык, в 1976 г. Всего записано 3 текста. В 1 ч. данного сборника, № 162; 
        &quot;Русская песня в Дагестане&quot;, № 319.<br>
