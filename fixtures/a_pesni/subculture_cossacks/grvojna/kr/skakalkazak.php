        Скакал казак через долину,<br>
        Через манжурские поля <br>
        Скакал он, всадник одинокий,<br>
        Блестит колечко на руке. <br>
        Кольцо казачка подарила, <br>
        Когда казак шел во поход. <br>
        Она дарила, говорила, <br>
        Что через год буду твоя. <br>
        Год прошел, казак стрелою <br>
        В село родное поскакал. <br>
        Завидел хату под горою, <br>
        Забилось сердце казака. <br>
        Навстречу старая старушка <br>
        Тихонько ему шепчет: <br>
        «Напрасно ты, казак, стремишься, <br>
        Напрасно мучаешь коня, <br>
        Тебе красотка изменила, <br>
        Другому верность отдала. <br>
        Скачи скорей до комиссара, <br>
        Пакет ему в руки отдай, <br>
        А сам обратно поскорее <br>
        С друзьями вместе погуляй». <br>
        Свернул казак коня направо <br>
        И в чисто поле поскакал. <br>
        <br>
        <em>Багизбаева М. М. Фольклор семиреченских казаков. Часть 2. Алма-Ата: 
        «Мектеп», 1979, № 10. Записана от Сычевой А. П., 1896 г. р., г. Иссык, 
        в 1976 г. Данный текст имеет дополнительное четверостишие (со слов «Скачи 
        скорей до комиссара"). Всего записано 30 текстов, почти полностью совпадающих, 
        за исключением укороченных. См.: «Фольклор семиреченских казаков ч. I, 
        № 10.</em><br>
        <br>
