        Семиреченский вариант популярной походной казачьей песни (см. дореволюционную 
        <a href="../../kazaki/skakalkazak.htm">&quot;Скакал казак через долину&quot;</a>). 
        Вариант послереволюционный, скорее всего, относится к периоду Гражданской 
        войны. В годы Гражданской войны варианты этой песни бытовали по всей Восточной 
        Сибири и Дальнему Востоку. Исполнялась одноголосно и хором.<br>
        <br>
