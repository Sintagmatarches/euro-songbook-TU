        <strong>ВАРИАНТ <br>
        <br>
        Скакал казак через долину</strong><br>
        <br>
        Скакал казак через долину,<br>
        Через Даурские поля <br>
        Скакал он горною тропинкой,<br>
        Кольцо блистало на руке. <br>
        Кольцо девица подарила, <br>
        Когда оставил дом родной. <br>
        Она дарила, говорила, <br>
        Что через год буду твоей. <br>
        Казак полгода не дождался, <br>
        В село родное поскакал,<br>
        Навстречу брат невесты вышел<br>
        И за поводья коня взял.<br>
        «Из далека видать приехал.<br>
        Измучил сильно ты коня.<br>
        А тебя сестрица ожидала, <br>
        Тебе лишь верная была. <br>
        Вчера японцы налетели,<br>
        Сестру изрезали ножом,<br>
        Мать изрубили, брата взяли,<br>
        Осиротел наш родной дом».<br>
        И партизан – казак ни слова,<br>
        Заворотил коня назад,<br>
        Уехал он в края далеки,<br>
        Чтоб за Советы жизнь отдать.<br>
        <em><br>
        Элиасов Л. Е. Народная революционная поэзия Восточной Сибири эпохи гражданской 
        войны. Улан-Удэ, 1957, стр. 111-112. Записана от Рыжакова в 1935 в Тарбагайском 
        аймаке БМАССР, с. Б. Куналей.</em><br>
        <br>
        <br>
        Вариант бытовал у прибайкальских партизан в годы Гражданской войны. <br>
        <br>
        Первая известная местная переделка этой песни в годы Гражданской войны 
        относится к концу 1917 – началу 1918 гг. – «Скакал горняк через долину» 
        (см.: А. Соколова. Материалы для изучения партизанской поэзии // «Сибирская 
        живая старина», вып. I (V), Иркутск, 1926, стр. 160. В этом варианте только 
        одно отличие от дореволюционных версий: последние две строки «повернул 
        казак налево, в обратный путь он поскакал» заменены на «уехал он в края 
        далеки, чтоб за свободу жизнь отдать», эти строки попали и в дальнейшие 
        варианты). Дореволюционный местный вариант см.: Старый фольклор Прибайкалья, 
        стр. 284.<br>
