	<h1>ПЕРВЫЙ РУССКИЙ ДЕСАНТ В АФРИКЕ</h1>
  
   

  <span class='i'>Нева. 2001. №8. С. 217-220.</span><br>
  <br><br>
  Интерес России к Африканскому континенту, особенно к восточному его побережью, 
  возник давно. Еще Петр Великий мыслил создать в одном из его береговых пунктов 
  перевалочную базу на пути к сказочной Индии, В конце прошлого века интересы 
  российской морской торговли стали упорно требовать решения этой проблемы. Понимали 
  это некоторые российские деятели, такие, как управляющий морским ведомством 
  адмирал И.А. Шестаков, русский консул в Египте М.А. Хитрово. К ним относился, 
  правда, по несколько иным соображениям и обер-прокурор Синода К.П. Победоносцев, 
  который считал, что создание базы на Африканском побережье будет содействовать 
  укреплению религиозных связей с Абиссинией, где большинство населения исповедовало 
  православие. <br>
  <br>
  К рассматриваемому периоду наиболее важные в экономическом плане регионы Африканского 
  континента были на тех или иных условиях поделены между Францией, Италией и 
  Англией. Наряду с этим вечно «дремлющая» Россия еще не сделала ни одного шага 
  для реального обеспечения своих интересов на этом континенте. Последнее в какой-то 
  степени объяснялось позицией по этому вопросу представителей правящей элиты, 
  считавших, что для матушки России и своих дел хватает без далекой Африки. Более 
  того, как писали петербургские газеты, апатия, опустошенность и равнодушие ко 
  всем важным событиям в мире, да и в самой стране, характеризовали состояние 
  так называемого высшего петербургского общества. На всем и на всех лежала тишина 
  бестолковой усталости, от которой люди как бы замертво уснули. <br>
  <br>
  Но вот на фоне гипнотической дремоты в столице России появилась совершенно новая 
  и необычайно колоритная фигура «коренастого, мордастого и рыжебородого атамана 
  вольных казаков» Николая Ивановича Ашинова с кинжалом на сером казакине, Он 
  с точки зрения исторической необходимости появился исключительно вовремя, громко 
  заявив всем, что хочет и может создать на Африканском побережье казацкое поселение, 
  названное им «Новой Москвой», Здесь же может быть образована база флота России 
  в Индийском океане. <br>
  <br>
  Ашинов всех уверял, что у него есть договор, по которому африканский султан 
  племени данакиль Магомет Лойта уступил ему в вечное владение участок побережья 
  залива Таджур, находящиися в нескольких десятках верст от Баб-эль-Мандебского 
  пролива, соединяющего Красное море с Индийским океаном. Вблизи от этого участка 
  побережья находился один из районов Абиссинии, дружелюбно расположенной к России, 
  что играло немаловажную роль.<br>
  <br>
  Это место для казацкой станицы и перевалочной базы российского флота было выбрано 
  атаманом, не имевшим специального образования, чрезвычайно удачно. Оно находилось 
  на прибрежной полосе залива, с трех сторон окруженной горами, способствующими 
  созданию благоприятного климата. Благодаря довольно высокой дневной температуре, 
  смягченной бризами и умеренно прохладными ночами, растительность на побережье 
  была обильной и разнообразной. Плодородная же земля позволяет собирать урожаи 
  овощей и фруктов несколько раз в году. <br>
  <br>
  Вместе с тем создание морской базы в Туджурском заливе давало возможность развертыванию 
  морской торговли. Кроме того, от побережья залива издавна существовал караванный 
  торговый путь к Шао и Харэре, находящийся в руках абиссинцев, дружелюбно относящихся 
  к русским. По этой дороге могут идти караваны с товарами в Туджур, где природа 
  создала прекрасные для стоянки кораблей гавань и рейд, закрытые от муссонов 
  островами и мысами, а от ветра со стороны суши — высокими горами. Последнее 
  делает выбранное место более предпочтительным по сравнению с Зейлой, занятой 
  англичанами, а также Обоком — базой французов.<br>
  <br>
  Появление в столице экзотического атамана Ашинова с его необычайно яркими, заманчивыми 
  и зовущими вдаль, к приключениям идеями буквально взорвало традиционное спокойствие 
  петербургского населения, О нем везде говорили и спорили. Его повсюду водили, 
  бесконечно расспрашивали и щедро угощали. Фигура атамана с его идеями затмила 
  все петербургские события. Для людей всех поколений, от детей до стариков и 
  от обывателей до сильных мира сего, он был, бесспорно, интересным субъектом, 
  появившимся как бы в качестве обновленного двойника исторически знаменитого 
  Афанасия Никитина, совершившего путешествие за три моря и написавшего об этом 
  книгу. Одни преклонялись перед ним, считая его героем и называя Ермаком, другие, 
  которых в таких случаях обычно бывает большинство, считали идеи и предложения 
  атамана бредовыми. Поэтому голоса в поддержку Ашинова тонули в хоре голосов 
  его недругов и просто остряков, превращая его предприятие в предмет насмешек 
  и издевательств. Так, корреспондент газеты «Одесские новости» писал об Ашинове 
  следующее: «Это действительно "вольный казак&quot; без профессии, без положения, 
  без средств, без жизненного опыта. В средние века ашиновыми комплектовались 
  ряды наемных ландскнехтов. Они бравировали теми же качествами, которые составляют 
  отличительные черты ашиновской натуры: удалью, беззаботностью и наглостью». 
  <br>
  <br>
  Естественно, что в таких условиях атаману Ашинову не удалось получить существенной 
  материальной и моральной поддержки в проведении задуманной операции по высадке 
  русских колонистов на побережье Таджурского залива. И все же весной 1888 года 
  он ее начал, но, к сожалению, она по обстоятельствам, не зависящим от атамана, 
  через год, в начале 1889 года, завершилась полным крахом. Операция проводилась 
  в два этапа, разделенным периодом в несколько месяцев. <br>
  <br>
  Первый этап, который по своему существу может быть назван «разведывательным 
  десантом», является предметом настоящего рассмотрения. <br>
  <br>
  Второй этап, связанный с последующей высадкой большой группы колонистов, достаточно 
  подробно рассмотрен в ряде публикаций А.В. Хренкова, руководителя проекта Академии 
  наук «Российская диаспора в Африке». <br>
  <br>
  Переходя к рассказу о разведывательном десанте русских в Африке, отметим, что 
  краткие сведения об этом этапе были обнаружены только в статьях «Беседа с вольным 
  казаком» («Петербургская газета», 2 июля 1888 г.) и «Рассказ бывшего ашиновца» 
  («Петербургская газета, 23 февраля 1889 г.). К сожалению, эти сведения оказались 
  весьма противоречивыми в части освещения масштабов операции и ее результатов. 
  <br>
  <br>
  Так, в интервью, данном Ашиновым репортеру газеты, высадка первой группы колонистов 
  представлялась как широкомасштабная десантная операция, а именно: «Мы ночью 
  высадились, а "они&quot; на следующую ночь напали. Царь Иван (негус Иоанн. — 
  Авт.) еще раньше знал, что мы высадились, так он по берегу своих разослал, они 
  нас ждали. Наших с абиссинцами было человек 400, а данакильского племени — 4000. 
  Султан остался жив и подписал договор». <br>
  <br>
  Расхваливал Ашинов в этом же интервью и жизнь первых колонистов на африканском 
  берегу: Казаки живут неплохо. Арбузов посеяли, капусты. Ну, скотина у нас всякая 
  — бараны и другая живность Рыбу ловим. Из моря даже часто жемчуг достаем. Живем 
  без жен, их потом привезем. Пока же обходимся барышнями, причем очень недурненькими». 
  <br>
  <br>
  Совсем иначе описал первую экспедицию один из ее участников, отставной фельдфебель 
  и георгиевский кавалер Степан Никитич Самусеев, бывший, кстати сказать, правой 
  рукой атамана. Его сообщение о состоянии первого десанта перед высадкой основной 
  группы колонистов полностью подтверждались одним из сподвижников атамана по 
  второй экспедиции — Аркадием Гордосевичем. <br>
  <br>
  Самусеев рассказывал, что в начале марта 1888 года в Одессе появился некий И.П. Зайцев, называвший себя, где только можно, представителем атамана Ашинова. 
  В гаванях, мастерских и на базарах он усердно вербовал среди разного люда добровольцев, 
  желавших участвовать в экспедиции славного атамана Ашинова в далекую африканскую 
  страну Абиссинию. В этой стране, говорил он, жить можно припеваючи, нужно только 
  быть храбрым. Он уверял всех, что уже был в этой чудесной стране и все видел 
  своими глазами: «Там настоящая благодать Божия. Про зиму там никто и не слыхивал, 
  а люди там не похожи на наших: черные, но очень богатые и живут себе роскошно. 
  Там фрукта разная растет себе круглый год. Лежишь себе под деревом навзничь, 
  а она прямо тебе в рот попадает, не ленись только толкнуть дерево ногой или 
  рукой».<br>
  <br>
  Зайцев заверял, что Ашинов всем завербованным будет щедро платить. А именно: 
  каждому положено месячное жалованье по 5 полуимпериалов (по 37 рублей 50 копеек 
  золотом. — Авт.). Кроме того, гарантировались за счет атамана хорошие харчи 
  и бесплатный проезд до места назначения, где все должны быть наделены плодородной 
  землей без всякой оплаты. Зайцев всячески убеждал всех своих слушателей принять 
  участие в экспедиции, говоря: «Вот, братцы, собирайтесь-ка вы к славному атаману 
  Ашинову да поезжайте с ним в Абиссинию». <br>
  <br>
  Несмотря на красочное описание прекрасных перспектив экспедиции и щедрых посулов 
  ее руководителей, желающих отправиться в жаркую и далекую Абиссинию, так непохожую 
  на родную матушку-Русь с ее зимними холодами и ненастной осенью, оказалось совсем 
  немного. Польстились на «златые горы» только Самусеев да бывший артиллерист 
  Литвинов. Вместе с Зайцевым они и поплыли из Одессы в Константинополь, где их 
  уже ожидал Ашинов со свитой из семи человек. Познакомившись с прибывшими, Ашинов 
  сказал им: «Ну вот мы, братцы, едем в Абиссинию. Вы должны меня слушаться и 
  во всем мне повиноваться. По пути мы вначале заедем к абиссинскому негусу Иоанну 
  (1) с царскими пакетами, да и на пирушку знатную: он скоро именины свои справлять 
  будет, ну и нас по-царски примет. Я наделю вас землей в достаточном количестве, 
  хорошее жалованье буду платить золотом, а вы должны будете состоять при мне 
  в конвое».<br>
  <br>
  Из Константинополя экспедиция отправилась на пароходе в составе нескольких человек. 
  Только в Порт-Саиде, находящемся на Средиземном море у входа в Суэцкий канал, 
  Ашинов нанял еще 11 африканцев, поручив командование ими Самусееву.<br>
  <br>
  В апреле 1888 года члены экспедиции сошли на берег Таджурского залива, и атаман 
  произнес для них короткую, но знаменательную речь, в которой, в частности, сказал: 
  «Тут, на этом пустынном берегу, через несколько времени будет город Новая Москва. 
  Со временем мы устроим дома, церковь, монастырь». <br>
  <br>
  Прошло не более двух недель, как Ашинов, поручив Самусееву смотреть за всем 
  «поселением», уехал в Россию за новой большой партией колонистов. Обещая вскоре 
  вернуться, атаман оставил всего 7 червонцев на жизнь оставленным в Африке 18 
  человек, из которых 11 африканцев (Самусеев уверял, что это были абиссинцы). 
  <br>
  <br>
  В первое время дела поселенцев хотя и с грехом пополам, но все же шли. Абиссинцы, 
  подчиненные Самусееву, интенсивно обучались ружейным приемам. Но когда закончились 
  деньги и были сьедены оставленные на пропитание продукты, они перестали ему 
  подчиняться и дело шло к бунту. <br>
  <br>
  Прошло 4 месяца, а атаман как в воду канул — поселенцы не получили от него ни 
  одной весточки. Дни тянулись, всех заели скука и тоска, потому что не было никакого 
  дела. Люди слонялись по берегу как неприкаянные. Абиссинцы забрали оружие и 
  тайно покинули лагерь. В таких условиях поселенцам ничего не оставалось, как 
  искать ближайшее европейское поселение. 27 августа 1888 года Самусеев и с ним 
  некто Федоровский, говоривший на нескольких европейских языках, добрались до 
  французской базы Обок, откуда были переправлены в Одессу.<br>
  <br>
  Так завершился первый этап создания русского поселения на берегу Африки, за 
  которым последовал второй... <br>
  <br>
  Завершая рассказ об этом важном историческом событии для России, оговорим, что 
  атаману Ашинову, кроме его авантюристических черт, были присущи большие организаторские 
  способности и талант. Ему, очевидно, пришлось для получения хоть какой-нибудь 
  реальной поддержки идти на некоторый обман в части приукрашивания результатов 
  первого десанта.<br>
  <br>
  Это был не безграмотный атаман, а человек, который глубоко и серьезно продумывал 
  все необходимые детали для создания долговременного русского поселения в Африке. 
  В частности, он подбирал людей, знавших язык далекой Абиссинии, для установления 
  тесных контактов с этои страной. Доказательством тому служат созданная Николаем 
  Ивановичем абиссинская азбука и начальный абиссино-русский словарь, выпущенный 
  в Петербурге в 1888 году (см. заглавные листы). Этот документ посчастливилось 
  найти в фондах Россииской национальной библиотеки. Так пусть же сохранится в 
  сердцах благородных потомков память об атамане Ашинове, совершившем столь необычный 
  исторический подвиг на благо России.<br>
