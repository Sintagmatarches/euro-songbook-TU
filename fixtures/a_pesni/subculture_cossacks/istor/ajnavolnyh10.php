<h1>&lt;РАЗИН ЕДЕТ В СИНЕ МОРЕ&gt; <br>
    <br>
  АЙ, НА ВОЛЬНЫХ СТЕПЯХ</h1>
  <br>
  Ай, на вольных степях, на степях Сара... на Саратовских,<br>
  <div class='refren'>Ей, что да повыше-то города ну, Камы... <br>
    Города было да Камы... города Камышина, </div>
  Ай, ну Камышина, <br>
   <div class='refren'>Ей, собиралися с Дону они люди во... <br>
  С Дону, братцы, люди да во... они люди вольные,</div>
  Ай, люди вольные:<br>
   <div class='refren'>Ей, все донские, братцы, они гребенски... <br>
  Братцы, казаки да яи... казаки яицкие, </div>
  Ай, ну яицкие,<br>
 <div class='refren'>Ей, середи, братцы, стоял вот атама... <br>
  Стоял, братцы, вот атама... стоял атаманушка, </div>
  Ой, атаманушка, <br>
   <div class='refren'>Ей, атаманушка, братцы, Степан Тимофе... <br>
  Стоял Степан вот Тимофе... Степан Тимофеевич, </div>
  Ай, Тимофеевич. <br>
   <div class='refren'>Ей, ну он речи-то вот говорил, <br>
  Братцы, будто во трубу, ровно во трубу трубил, </div>
  Ай, во трубу трубил: <br>
  <div class='refren'>„Ей, собирайтеся вы, братцы, да ребятушки, <br>
   Вот бы только во еди... братцы, во единый круг, </div>
  Ай, во единый круг, <br>
 <div class='refren'>Ей, ну давайте мы думушку да поду... <br>
    Думушку, братцы, да поду... думушку подумаем, </div>
  Ай, ну подумаем: <br>
  <div class='refren'>Ей, вот ну поедем же, братцы во синё, <br>
   Гулять, братцы, вот во синё, гулять, братцы, во синё море,</div>
  Ай, во сине море. <br>
  <div class='refren'>Ей, разобьем-то мы, братцы, вот басурма... <br>
 Басурманские да кора ... вот всё кораблики, </div>
  Ай, ну кораблики, <br>
  <div class='refren'>Ей, заберём, братцы, казны вот сколько на…<br>
   Казны, братцы, сколько на… казны сколько надобно,</div>
  Ай, сколько надобно,<br>
   <div class='refren'>Ей, мы поедемте, братцы, вот в кременну,<br>
   Поедемте, братцы, в кременну, вот мы в кременну Москву,</div>
  Ай, в кременну Москву.<br>
   <div class='refren'>Ей, мы закупим себе платьица цве…<br>
 Закупим там платьица цве… вот платьица цветные,</div>
  Ай, платья цветные,<br>
  <div class='refren'>Ей, поплывем же вниз по Волге ма…<br>
    Поплывем по Волге да ма… вниз по Волге матушке&quot;.</div>
