<h1>&lt;ШТУРМ АЗОВА&gt;<br>
<br>
АХ БЕДНЫЕ ГОЛОВУШКИ СОЛДАТСКИЕ...</h1>
	<br>
Ах бедные головушки солдатские,<br>
  Как ни днем, ни ночью вам покою нет! <br>
  Что со вечера солдатам приказ отдан был, <br>
  Со полуночи солдаты ружья чистили, <br>
  Ко белу свету солдаты во строю стоят. <br>
  Что не золотая трубушка вострубила, <br>
  Не серебряна сиповочка возыграла, <br>
  Что возговорит наш батюшко, православный царь: <br>
  «Ах вы гой еси, все князья и б<strong>о</strong>яря! <br>
  Вы придумайте мне думушку, пригадайте, <br>
  Еще как нам Азов-город взяти?» <br>
  Еще князья и б<strong>о</strong>яря промолчали: <br>
  Еще ж сам наш батюшка прослезился: <br>
  «Ах вы гой еси, солдаты и драгуны! <br>
  Вы придумайте мне думушку крепкую, <br>
  Еще как-то нам Азов-город взяти?» <br>
  Как не ярые пчёлушки зашумели, <br>
  Что взговорят солдаты и драгуны: <br>
  «Взять ли нам, не взять ли — белой грудью!»<br>
  На восходе было красного солнышка, <br>
  На закате было светлого месяца, <br>
  На зоре они на приступ пошли, <br>
  Под тот ли под славной под Азов-город, <br>
  Что под те ли стены белокаменные, <br>
  Ах под те ли под раскаты под высокие. <br>
  Что не с гор ли белы камни покатилися, <br>
  Покатилися со стен неприятели; <br>
  Не белы снеги в поле забелилися, <br>
  Забелилися белы груди басурманские;<br>
  Ах не с дождичка ручьи разливалися, <br>
  Разливалася тут кровь нечестивая. <br>
<br>
