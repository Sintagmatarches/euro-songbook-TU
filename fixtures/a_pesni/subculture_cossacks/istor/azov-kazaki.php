 <h1>&lt;КАЗАКИ ПОД 
АЗОВОМ&gt;<br>
  <br>
У НАС, БРАТЦЫ, НА ДОНУ...</h1> 

 <br>
 У нас, братцы, на Дону, 
  во Черкасском городу, <br>
  Проявилась у нас, братцы, прирожоная тума, <br>
  Он из тум, братцы, тума, Сенька Маноцков злодей, <br>
  Крепкой думушки со стариками он не думывал, <br>
  Думывал крепкую он думушку с ярыжками. <br>
  Перекинулся, собака, ко азовскому паше, <br>
  А азовский-то паша стал его спрашивать: <br>
  «Ты скажи, скажи, приятель, правду истинную – <br>
  Что-то думают у вас во Черкасском городу?» — <br>
  «Да у нас-то на Дону, во Черкасском городу, <br>
  Старики-то пьют-гуляют, по беседушкам сидят, <br>
  По беседушкам сидят, про Азов ваш говорят: <br>
  «Ой не дай боже азовцам ума-разума того, <br>
  Не поставили б они башенки на усть-речки Каланчи, <br>
  Не перекинули бы цепи через славный тихий Дон, <br>
  Не подвели бы они струны ко звонким колоколам. <br>
  Уж нельзя нам, братцы, будет во сине море пройтить, <br>
  По синю морю гулять, зипунов-то доставать». <br>
  Как у нас было на Дону, во Черкасском городу, <br>
  Войсковой наш атаман во всю ночушку не спал, <br>
  Как со вечеру сокол наш Роговые проплывал, <br>
  Ко белу свету сокол наш, по синю морю гулял, <br>
  По синю морю гулял, кораблики разбивал. <br>
<br> 
<span class='i'>Казак Левка Маницкий перебежал в Азов к турецкому паше (потом воротился и был 
  расстрелян в Черкасске). Это событие предшествовало осаде Азова 1696 года.<br>
  <br>
  Тум<strong>а</strong> - выродок. Яр<strong>ы</strong>жка - низший служитель в 
  московских приказах, выполнявший полицейские функции; пьяница, беспутный человек. 
  Зипун<strong>ы</strong> доставать - захватывать военную добычу.<br>
  <br>
   Исторические песни. Баллады / Сост., подг. текстов, вступ. статья 
