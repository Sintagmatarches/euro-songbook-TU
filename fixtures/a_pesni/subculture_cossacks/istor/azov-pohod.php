<h1>&lt;ПОХОД К АЗОВУ&gt;<br>
<br> 
НАМ ПОРА-ТО, РЕБЯТУШКИ, К КАБАКУ ИДТИ!..</h1>
	<br>
Нам пора-то, ребятушки, к кабаку идти! <br>
  Не вечерняя заря потухать стала; <br>
  Не утренняя заря занимается, <br>
  Бежит-то из Москвы скорый посол, <br>
  Держит во руках грозный указ, <br>
  Чтобы были мы, солдатушки, приубр<strong>а</strong>нные, <br>
  Перевязи-портупеи были бы белёные <br>
  И ружьецы были бы чищёные. <br>
  Нам заутра, солдатушкам, в поход идти, <br>
  В поход идти под Азов-город, <br>
  Нам стоять-то, солдатушкам, под Азовом три года. <br>
<br>
<span class='i'>
  Петр I, правивший самостоятельно с 1689 года, в 1695 году предпринял первый 
  Азовский поход, но взять крепость не удалось. В следующем году, построив военный 
  флот, Петр снова отправился с войском и флотом к Азову.<br>
