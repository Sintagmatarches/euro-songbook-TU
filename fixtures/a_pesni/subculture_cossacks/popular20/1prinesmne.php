<br>
<strong>2. Раз принес мне барин чаю</strong><br>
<br>
Раз принес мне барин чаю <br>
И велел его, сварить, <br>
А я отроду не знаю, <br>
Как проклятый чай варить. <br>
Вот я взял полфунта чаю, <br>
Положил его в горшок, <br>
На прибавку перцу, луку <br>
И моркови корешок. <br>
Все поставил на таганку, <br>
Раза три прокипятил <br>
И для пущею для серту <br>
Сверху, маслица пустил. <br>
Вот я взял тарелку, ложку <br>
И поставил все на, стол <br>
К тому времю понемножку <br>
Тут и барин подошел. <br>
«Ты послушай-ка, наш барин, <br>
Хочешь чай принять сейчас?» <br>
«Если будешь так стараться, <br>
Будешь чай варить опять, <br>
То на праздничек, наверно, <br>
Подарю рублевок пять». <br>
«Пять рублей ведь денег много, <br>
С ними можно погулять. <br>
И Машка-недотрога <br>
Меня будет уважать». <br>
Слышу барин расходился, <br>
Меня в комнату позвал, <br>
В волоса мои вцепился <br>
И таскал меня, таскал. <br>
Долго, долго он сердился, <br>
Злой по комнате ходил <br>
Тут чебык ему попался — <br>
Меня в кухню проводил. <br>
Долго думал я, старался, <br>
Чем ему не угодил? <br>
А потом лишь догадался, <br>
Что я чай не посолил.<br>
