	<span class="header"><strong>РАЗ 
ПРИНЕС МНЕ БАРИН ЧАЮ</strong><br></span>
<br>
Раз принес мне барин чаю<br>
И велел его сварить,<br>
А я отроду не знаю,<br>
Как проклятый чай варить.<br>
<br>
Вот поставил я таганку,<br>
Чай весь высыпал в горшок,<br>
Дал приправы – перцу, луку<br>
И петрушки корешок.<br>
<br>
А потом все это в печку,<br>
Раза три прокипятил<br>
И для пущего эффекту<br>
Сверху маслица подлил.<br>
<br>
«Чай готов! Извольте кушать» -<br>
Снял я с барина пальто.<br>
- Молодец, коль будешь слушать. –<br>
И сказал. – Хвалю за то!<br>
<br>
Если будешь аккуратно<br>
Ты всегда так исполнять,<br>
Через месяца четыре<br>
Подарю рублевок пять.<br>
<br>
Ой, пять рублей ведь денег много,<br>
С ними можно погулять,<br>
И Акулька-недотрога<br>
Меня будет уважать.<br>
<br>
Вдруг… мой барин разозлился,<br>
Меня в горницу позвал,<br>
В волоса мои вцепился<br>
И таскал меня, таскал.<br>
<br>
«Ах ты, пешка. Ах, дубина,<br>
ты чего ж мне наварил,<br>
Ах ты, чертов образина,<br>
Чтобы пес тебя схватил!»<br>
<br>
Долго, долго он ругался,<br>
Злой по горнице ходил.<br>
Тут чурбак ему попался,<br>
Им на кухню проводил.<br>
<br>
Я все думал, удивлялся,<br>
Чем ему не угодил,<br>
И потом лишь догадался,<br>
Что я чай… не посолил…<br>
<br>
<em>В нашу гавань заходили корабли. Вып. 5. М.: Стрекоза, 2001.</em><br>
<br><br>
Типичная сатирическая песенка из дешевых песенников начала XX в.<br>
