<strong>ВАРИАНТЫ (2)<br>
<br>
1. Раз купил мне барин чая</strong><br>
<br>
Раз купил мне барин чая<br>
И велел его сварить,<br>
А я отроду не знаю<br>
Как проклятый чай варить.<br>
<br>
Вот я взял пол-фунта чая<br>
И весь высыпал в горшок.<br>
Положил я луку, хрену<br>
И морковки корешок.<br>
<br>
На таган я все поставил<br>
Раза три прокипятил<br>
И для пущего эффекта<br>
Сверху маслицем полил.<br>
<br>
Снял горшок я, сажу вытер<br>
И поставил все на стол.<br>
Положил я ложку, вилку;<br>
Тут и барин подошёл.<br>
<br>
Чай готов, извольте кушать<br>
Угодил теперь я Вам!<br>
- Молодец, коль будешь слушать<br>
Пять рублей на водку дам.<br>
<br>
Пять рублей ведь денег много<br>
На них можно погулять<br>
И Акулька-недотрога<br>
Меня будет уважать.<br>
<br>
Слышу барин расходился,<br>
Меня в горницу позвал<br>
В волоса в мои вцепился<br>
И таскал меня, таскал...<br>
<br>
- Ах ты, сукин сын, бродяга,<br>
Что ж ты чаю наварил!<br>
Тут попалась ему палка <br>
Вплоть до кухни проводил.<br>
<br>
Полетел я кверх-тормашкой<br>
Вплоть до самых до дверей.<br>
Вот тебе моя Акулька,<br>
Вот тебе и пять рублей.<br>
<br>
Вот я думал все, старался,<br>
Чем не смог я угодить?<br>
А потом я догадался -<br>
Ведь забыл я посолить.<br>
<br>
<em>Московский вариант, дореволюционный, со слов Е. Н. Фатовой (1900-1991). 
Получено 14.12.2004, спасибо Олегу Воробьеву &lt;olvoal@yahoo.com&gt;</em><br>

