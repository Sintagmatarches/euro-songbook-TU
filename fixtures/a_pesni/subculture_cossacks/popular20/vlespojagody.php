	<span class='bold'>2. На пригорке, в частом лесе </span><br>
  <br>
  На пригорке, в частом лесе <br>
  Береза стоит. <br>
  Как под этой под березой <br>
  Девица сидит. <br>
  Она грибов не собирает, <br>
  Она цветы не рвет, <br>
  Об одном она мечтает, <br>
  Все кого-то ждет. <br>
  Вдруг в кустах раздался шорох, <br>
  Бедная дрожит, <br>
  Перед ней красивый парень, <br>
  Молодой стоит: <br>
  «Что ж ты ждешь, моя родная?» — <br>
  Парень ей сказал <br>
  И принял ее в объятья, <br>
  Нежно целовал. <br>
  «Отпусти, ты, друг мой милый, <br>
  Душу не губи, <br>
  А другую, городскую, <br>
  Лучше полюби. <br>
  Городскую ты полюбишь, <br>
  Счастлив будешь ты. <br>
  Меня, бедную крестьянку, <br>
  Позабудешь ты». <br>
  «Нет, тебя я не забуду, <br>
  Милая моя, <br>
  Как вернусь, так обвенчаюсь, <br>
  Возьму за себя». <br>
  Тут он с клятвой роковою <br>
  С руки перстень снял, <br>
  Пояс шелковый и перстень – <br>
  Все он ей отдал. <br>
  Тут уж осень наступила, <br>
  Милого все жду, <br>
  То на пояс, то на перстень <br>
  Я с тоской гляжу. <br>
  Чтоб спастися от позора,<br>
  Грозного отца, <br>
  Жизнь покончила девица, <br>
  Бедная она. <br>
  На пригорке в частом лесе <br>
  Береза стоит, <br>
  Как на этой на березе <br>
  Труп ее висит. <br>
  Пояс шелковый ей туго <br>
  Шею перьтянул, <br>
  Перстенек, милым дареный, <br>
  На руке блеснул. <br>
  Вот вам, девушки, наука, <br>
  Как в лесок ходить, <br>
  И другая вам наука, <br>
  Как ребят любить. <br>
  <br>
<span class='i'>Записана от 
