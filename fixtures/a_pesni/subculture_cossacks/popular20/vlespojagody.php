  <span class='bold'>3. За грибами в лес девчата...<br></span>
<br>
За грибами в лес девчата<br>
  Гурьбой мобрались.<br>
  Как дошли они до леса,<br>
  Так все разбрелись.<br>
  <br>
  Кто грибы там собирает,<br>
  Кто цветочки рвёт,<br>
  А одна из них девчонка<br>
  Всё кого-то ждёт.<br>
  <br>
  Вдруг в кустах раздался шорох,<br>
  Бедная дрожит.<br>
  Перед ней красивый парень<br>
  Городской стоит.<br>
  <br>
  - Здравствуй, здравствуй, дорогая, -<br>
  Парень ей сказал.<br>
  И, обняв её в объятья,<br>
  Крепко целовал.<br>
  <br>
  - Не цедуй меня, мой милый,<br>
  Душу не губи.<br>
  А другую, городскую<br>
  Лучше полюби.<br>
  <br>
  Городская покрасивей<br>
  И на вид умней.<br>
  Меня, бедную крестьянку,<br>
  Позабудь скорей.<br>
  <br>
  - Нет же, нет же, дорогая,<br>
  Не забуду я.<br>
  Прийдёт осень, я вернуся<br>
  И возьму тебя.<br>
  <br>
  Пришла осень, его нету,<br>
  А она всё ждёт.<br>
  Её, бедную крестьянку,<br>
  Замуж не берёт.<br>
  <br>
  На поляне возле леса<br>
  Берёза стоит.<br>
  А под этою берёзой<br>
  Девица лежит.<br>
  <br>
  Пояс шёлковый, зелёный<br>
  Шею ей стянул,<br>
  Перстень, милым подарённый,<br>
  На руке блеснул.<br>
  <br>
  Вот вам, девушки, наука,<br>
  Как у лес ходить.<br>
  А ещё одна наука -<br>
  Городских любить.<br>
  <br>
