 <span class='bold'>ВАРИАНТЫ (3)<br>
  <br>
  1. За грибами в лес девчонки 
	 <br></span><br>
	 За грибами в лес 
  девчонки <br>
  Гурьбой собиралися, <br>
  Как дошли до чащи леса – <br>
  Все поразбрелися. <br>
  И грибов не собирают, <br>
  Топчут там траву, <br>
  И по лесу раздается <br>
  Весело: «А-у». <br>
  И грибов не собирает, <br>
  И цветов не рвет, <br>
  Об одном она мечтает, <br>
  Все кого-то ждет. <br>
  Вдруг в кустах раздался шорох, <br>
  Бедная дрожит, <br>
  Перед ней красивый парень, <br>
  Молодой стоит: <br>
  «Ждешь ты, ждешь меня, желанная», — <br>
  Ласково сказал. <br>
  И привлек ее в объятия, крепко целовал. <br>
  «Не целуй меня, желанный, душу не губи, <br>
  А другую, городскую, лучше полюби». <br>
  На пригорке в чаще леса <br>
  Береза стоит, <br>
  А как на этой на березе <br>
  Труп ее висит.<br>
  Пояс шелковый с кистями <br>
  Шею перьтянул,<br>
  Перстень, другом подаренный,<br>
  На руке блеснул.<br>
  Вот вам, девушки, наука,<br>
  Как в лесок ходить,<br>
  А другая вам наука<br>
  Как казаков любить.<br>
	<br>
	<span class='i'>Записана от 
  Михайловой О.И., 1905 г. р., г. Талгар, в 1976 г. Всего записано 6 вариантов.<br>
