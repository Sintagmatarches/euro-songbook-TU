<h1>В 
ЛЕС ПО ЯГОДЫ ДЕВЧАТА</h1>
<br>
 В лес по ягоды девчата гурьбой собрались,<br>
  Как дошли они до леса, так все разбрелись.<br>
  Ходят, ищут, собирают, топчут лист, траву,<br>
  А по лесу раздается весело «ау»!<br><br>Лишь одна не собирает, ягоды не рвет,<br>
  Об одном она мечтает и кого-то ждет.<br>
  Вдруг кусты зашевелились, вздрогнула она,<br>
  Перед ней красивый парень белого лица.<br><br>«Что грустишь ты, моя милка?» - он тут ей сказал,<br>
  И привлек ее в объятья, да поцеловал.<br>
  «Не целуй меня, мой милый, душу не губи,<br>
  А другую, городскую, лучше полюби».<br><br>«Мне не надо городскую, милая моя,<br>
  Придет осень, я приеду, возьму за себя».<br>
  Вот и осень наступила, а она все ждет,<br>
  Под ее больным сердечком горюшко растет.<br><br>И боялась она сильно грозного отца,<br>
  Шарф накинула на плечи, в лес гулять пошла.<br>
  На опушке, между сосен, береза стоит,<br>
  А на той большой березе девушка висит.<br><br>Пояс шелковый с кистями шею ей стянул,<br>
  Подарённый другом перстень на руке блеснул.<br>
  Вот вам девушки, наука, как в лесок ходить,<br>
  А другая вам наука, как ребят любить.<br>
  <br>
