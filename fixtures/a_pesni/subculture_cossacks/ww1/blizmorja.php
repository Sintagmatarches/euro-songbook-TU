  2. В долине турецкой границы</strong><br>
  <br>В долине турецкой границы,<br>
	  На склоне высокой горы<br>
	  Там рвутся снаряды – гранаты<br>
	  С утра до вечерней поры. <br>
  <br>Там бьются казаки за правду<br>
      И смерть не страшна храбрецам.<br>
      А дома отец во кручине<br>
      И с ним пригорюнилась мать. <br>
  <br>Читают газеты о битве,<br>
      Желают о сыне узнать.<br>
      А сын ваш убит на разведке, -<br>
      Вещает военный приказ. <br>
  <br>Убит он турецкой гранатой<br>
      И кости в долину вросли.<br>
      Лежит он во чистом во поле,<br>
      Зеленый бешмет весь в крови. <br>
  <br>Никто не найдет той могилы<br>
      Защитника русской земли.<br>
      Опять мы пойдем в бой, ребята.<br>
      Опять мы докажем врагу. <br>
