<strong>ВАРИАНТЫ (3)<br>
<br>
1. Новая песня</strong><br>
<br>
От русско-германской границы<br>
До Вислы широкой реки<br>
Там льются кровавы потоки<br>
С утра до вечерней зари.<br>
<br>
Там слышны глухие раскаты,<br>
У пушек всех жерлы горят,<br>
Там рвутся шрапнели, гранаты<br>
И землю взрывает снаряд.<br>
<br>
Там бьются за правое дело,<br>
И смерть храбрецов не страшит,<br>
И в битву идут они смело:<br>
Их Божия матерь хранит.<br>
<br>
Отец, удрученный слезою,<br>
И с ним пригорюнившись мать -<br>
Читают про бой отдаленный:<br>
Про сына им хочется знать.<br>
<br>
Из армии им сообщают<br>
(Нахлынули слезы из глаз),<br>
Что сын их убит был врагами,<br>
Об нем по полку был приказ.<br>
<br>
Убит он во цвете и силе,<br>
Убит, похоронен вдали...<br>
Никто той могилы не знает<br>
Защитника русской земли.<br>
<br>
<em>В.И. Симаков. Прапорщик. Новейший военный песенник. 2-е испр. и дополн. издание. Пг.: Издание книжной торговли Н.И. Холмушина, 1916. [Разрешено военной цензурой за №4643, 7 мая 1916 г.]. С. 5-6.</em><br>
