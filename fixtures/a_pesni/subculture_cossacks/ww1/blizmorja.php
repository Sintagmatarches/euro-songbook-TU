ГЕРМАНСКОЙ ГРАНИЦЫ </strong></span>
<br>
<br>
Близ моря, германской границы,<br>
  В ущельях кремнистой скалы,<br>
  Там льются кровавы потоки<br>
  С утра до вечерней зари.<br>
<br>Там русское войско дерется<br>
  За честь и за веру, царя…<br>
  Идут они в бой обреченны,<br>
  Но смерть храбрецам не страшна.<br>
<br>А дома отец при кручине<br>
  И с ним престарелая мать<br>
  Читают военны газеты,<br>
  Желают о сыне узнать.<br>
<br>Из Люблина, пишут газеты…<br>
  И брызнули слезы из глаз,<br>
  Что сын их убитый врагами,<br>
  А кости зарыты в горах.<br>
<br>Что кости в лесу их зарыты,<br>
  Вдали от родимой семьи…<br>
  Никто не найдет той могилы<br>
  Защитника русской земли.<br>
<br>И, колыбельку качая,<br>
  Склонилась над детищем мать;<br>
  И мужа жена вспоминает,<br>
  И катятся слезы из глаз.<br>
<br>А дети играют, смеются,<br>
  Не знают, о чем плачет мать.<br>
  А вырастут, спросят: где папа,<br>
  Хотим мы его повидать.<br>
<br>А папы давно нет на свете,<br>
  Убит подо Львовом в бою,<br>
  Оставил одних он вас, дети,<br>
  Живите на славу свою.<br>
<br>
