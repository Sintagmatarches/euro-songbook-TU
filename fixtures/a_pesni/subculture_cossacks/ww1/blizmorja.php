  <br>
   Близ Риги на северном фронте <br>
   В долине песчаной земли,<br>
   Там льются кровавы потоки<br>
  С утра до вечерней зари.<br>
   Там бьются сибирцы за правду,<br>
  И смерть храбрецам не страшна,<br>
   А дома отец при кручине,<br>
  И с ним, пригорюнившись, мать,<br>
   Читают военны газеты<br>
  И хочут о сыне узнать.<br>
   Из Риги им пишут о сыне:<br>
  Был издан военный прказ,<br>
    Что сын их убит на разведке,<br>
   Лежит его тело вдали,<br>
  Никто не узнает в могиле<br>
   Защитника русской земли.<br>
     А дома жена молодая,<br>
   Склонившись над малым дитёй,<br>
  И горьки слез<strong>ы</strong> проливает,<br>
   Как вспомнит о муже своем.<br>
     Он весело-бодро прощался,<br>
   Склонившись над милым дитёй.<br>
  Теперь лежит камень тяжелый<br>
   На сердце навеки у нёй.<br>
