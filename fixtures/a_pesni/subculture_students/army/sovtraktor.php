  <span class='bold'>2. Когда родной колхоз был занят важным делом </span><br>
  <br>
  Когда родной колхоз был занят важным делом -<br>
  Посеять на полях гречиху поскорей<br>
  Советский мирный трактор подвергся артобстрелу<br>
  Шести крупнокалиберных китайских батарей.<br>
  <br>
  Он отстегнул прицеп и запустил локатор,<br>
  Работу автоматики проверил поскорей.<br>
  Ответил тракторист одним могучим залпом<br>
  И уничтожил шесть китайских батарей.<br>
  <br>
  А после, слив бензин и разогнав реактор,<br>
  Он быстро допахал гречиху и овёс.<br>
  Поднялся в небеса советский мирный трактор<br>
  И полетел обратно в свой родной колхоз.<br>
  <br>
  А после, в интервью какой-то из редакций,<br>
  Наш бригадир сказал, как будто невзначай,<br>
  Что в случае повторения подобных провокаций<br>
  На поле вместо трактора мы выпустим комбайн.<br>
