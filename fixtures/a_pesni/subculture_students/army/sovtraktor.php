	<h1>СОВЕТСКИЙ МИРНЫЙ ТРАКТОР </h1><br>
  Я расскажу о вопиющем факте:<br>
  На берегах Амура, средь полей<br>
  Попался наш простой советский трактор<br>
  В прицел шести китайских батарей.<br>
  <br>
  Ударил залп, снаряды полетели,<br>
  Но тракторист был парень с головой:<br>
  Он жмёт педаль - и вот не видно цели<br>
  В поставленной завесе дымовой.<br>
  <br>
  А трактор взмыл над милой стороною<br>
  И в тот же миг агрессору в ответ,<br>
  Чтоб постерёгся нас пугать войною<br>
  Ударил залп тактических ракет.<br>
  <br>
  А тракторист наш, капитан Литвинов,<br>
  Взглянул на карту и включил форсаж,<br>
  Спокойно отбомбился над Пекином<br>
  И заложил на родину вираж.<br>
  <br>
  Он над Амуром выключил реактор,<br>
  Чтоб не пугать родных овец и коз.<br>
  Пронёсся в небе наш советский трактор<br>
  На дозаправку в свой родной колхоз.<br>
  <br>
  И, если враг опять предпримет меры,<br>
  Чтоб помешать собрать нам урожай,<br>
  Приказом Агропром СССР-а<br>
  У нас на поле вылетит комбайн<br>
  (С вертикальным взлётом).<br>
  <br>
  <br>
  Шуточная песня времен  конфронтации СССР и КНР. Пиком конфронтации стал вооруженный конфликт на острове Даманский в марте 1969 г. Синоним этой песни - анекдот <em>&quot;Китайцы обстреляли мирно пашущий советский трактор. Трактор уничтожил 
  противника арт-огнём и улетел на базу&quot;</em>. Автор приведенной выше обработки - Игорь 
  Байков, она выполнена 5-стопным ямбом и навеяна песней   <a href="../ww2/oficial/3tankista.php">"Три танкиста"</a>, сложенной 5-стопным хореем. Размеры эти ритмически совместимы, и на &quot;Трех танкистов&quot; ее можно спеть. Однако канонический размер &quot;Советского трактора&quot; - 6-стопный цезурированный ямб (см. все остальные варианты ниже), и петь эти версии на &quot;Трех танкистов&quot; нельзя. Из широко известных песен 6-стопным цезурированным ямбом сложена, например, <a href="../dvor/sirentuman.php">"Сиреневый туман"</a>. А вообще, это один из первых русских песенных силлабо-тонических размеров, он в ходу с середины XVIII в. (см., например, романс Г. Теплова на слова А. Сумарокова  <a href="../18/teplov10.php">"К тому ли я тобой, к тому ли я пленилась..."</a>).<br>
