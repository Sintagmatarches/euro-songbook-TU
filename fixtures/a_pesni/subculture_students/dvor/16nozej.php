        <br>
        Бытует под разными заглавиями, в том числе &quot;Шестнадцать столовых 
        ножей&quot; и &quot;Мотор колеса крутит&quot;. Поется на мотив <a href="../popular20/razlukaty.htm">&quot;Разлука 
        ты, разлука&quot;</a>.<br>
        <br>
        Пародия советских времен на песню <a href="marusja-vetch.php">&quot;Маруся&quot;</a> 
        (&quot;Вечер вечереет...&quot;), одну из появившихся в начале 1910-х гг. 
        песен о некой Марусе, покончившей с собой от несчастной любви &quot;Маруся&quot; 
        также поется на мотив &quot;Разлука ты, разлука&quot;. Упоминание крематория 
        - очень необычная черта для дворового фольклора. Она позволяет предположительно 
        датировать песню концом 1920-х гг.: первый советский постоянно действующий 
        крематорий был построен в 1927 г. на территории Донского монастыря, и 
        это нововведение бурно обсуждалось.<br>
        <br>
        В дореволюционных песнях про Марусю девушка травится, но здесь пародируется 
        другой популярный в городских романсах способ девичьего самоубийства - 
        девушка хватает <em>&quot;ножик со стола и в молодую грудь вонзает&quot;</em>. 
        Этот <em>&quot;ножик со стола&quot; </em>превратился  в 
        <em>&quot;шишнадцать столовых ножей&quot;</em>. Не исключено, что и 
        в дореволюционных песнях про Марусю встречались варианты с закалыванием ножом - 
        просто они были менее популярны и не сохранились.<br>
        <br>
        Другие дореволюционные песни про Марусю - <a href="marusja.php">&quot;Маруся 
        отравилась&quot; (&quot;Вот солнце закатилось...&quot;</a>, 1911, это 
        наиболее известная песня) и <a href="marusja-zizn.php">&quot;Маруся отравилась&quot; 
        (&quot;Жизнь так несложна, крайне так проста...&quot;</a>, 1913, поется 
        на другой мотив). См. также песню на аналогичный сюжет <a href="../popular20/kolja-manja.php">&quot;Гуляли 
        Коля с Манею&quot;</a>: в ней парень бьет девушку за отказ выйти за него 
        замуж, и та умирает в больнице.<br>
        <br>
        Подробнее о цикле песен про Марусю см.: Сергей Неклюдов <a href="moskva/a-marusia.php">&quot;Почему 
        отравилась Маруся?&quot;</a> (2008).<br>
