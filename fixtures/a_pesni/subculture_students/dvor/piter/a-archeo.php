        Государственный мемориальный историко-литературный и природно-ландшафтный 
        музей-заповедник А. С. Пушкина &quot;Михайловское&quot;; Санкт-Петербургский 
        Государственный университет культуры и искусств. Кафедра музееведения; 
        Институт истории материальной культуры Российской Академии наук. Санкт-Петербург, 
