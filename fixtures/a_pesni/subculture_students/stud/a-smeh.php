        раскрасить физиономию во все цвета радуги и попугать своим видом соседей 
        по общежитию и одиноких прохожих на прилегающих к общаге улочках. Впрочем, 
        и хронологически, и по сути этот праздник (31 октября) близок тому, что 
        происходило (а кое-где и происходит) в традиционном украинском селе на 
