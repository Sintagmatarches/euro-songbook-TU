        бешкетували в тих хатах, де дівчата, знімали хвіртки та закидали на дах” 
        [13, с. 46]. Кстати, маска из гарбуза с горящей свечкой внутри часто была 
        атрибутом осенних «лякалок», устраиваемых парнями.<br>
        <br>
        Среди новых локальных традиций отметим также праздник <strong>День группы</strong>, 
        который отмечается в Киевском национальном педуниверситете. Если номер 
        группы 11, то праздник ее – 11.11. «Схемы» празднества никакой нет, отмечают 
        – кто как хочет, чаще всего просто веселой гурьбой идут пить пиво.<sup>9</sup><br>
        <br>
        В заключение отметим, что современная студенческая ритуалистика тесно 
        связана с традиционной народной культурой (в т.ч. смеховой) и вобрала 
        в себя многие ее элементы. Обнаруживаются параллели и с «соседними» субкультурами 
        – школьной, армейской, спортсменской, музыкантской и др. Карнавальное 
        начало, присущее студенческим праздникам, – вполне закономерное явление, 
        т.к. серьезность дела, которым вынуждены заниматься молодые люди («тяжело 
        в учении…» – как говаривал Суворов), не может не требовать противоположного 
        полюса – разрядки и безудержного веселья. Впрочем, студент – существо, 
        готовое в любой сверхсерьезной ситуации найти нечто комическое и мгновенно 
        изменить ее характер. Студенческая субкультура «не переваривает» занудства, 
        скуки, тупости, псевдосакрализации – идущих «сверху», но она и предельно 
        самокритична. Смех для нее – естественная стихия существования, привычная 
        форма бытия, призма, сквозь которую воспринимаются как глобальные события, 
        так и повседневные явления. Поэтому исследование студенческой смеховой 
        культуры во всем многообразии ее проявлений, во всем спектре ее жанровых 
        и ассоциативных связей может дать ключ к истинному пониманию данной субкультуры.<br>
