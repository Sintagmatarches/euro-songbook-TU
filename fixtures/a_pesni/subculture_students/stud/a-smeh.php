        <span class="text"><strong>Михаил Красиков</strong><br>
        <br>
        <span class="header"><strong>ОБРЯДОВО-ЗРЕЛИЩНЫЕ ФОРМЫ СМЕХОВОЙ КУЛЬТУРЫ 
        СОВРЕМЕННОГО УКРАИНСКОГО СТУДЕНЧЕСТВА (К ПОСТАНОВКЕ ПРОБЛЕМЫ)</strong></span> 
        <br>
        <br>
        <em><br>
        Красиков М.М. Обрядово-зрелищные формы смеховой культуры современного 
        украинского студенчества. (К постановке проблемы) // Δờεа / Докса. Збірник 
        наукових праць з філософії та філології. Вип. 9: Семантичні й герменевтичні 
        виміри сміху. – Одеса, 2006. – С. 61-69.</em><br>
        <br>
        <br>
        Понятие «смеховая культура», «смеховой мир» со времени публикации классических 
        работ М.М. Бахтина «Франсуа Рабле и народная культура средневековья и 
        Ренессанса» и Д.С. Лихачёва, А.М. Панченко, Н.В. Понырко «Смех в Древней 
        Руси» прочно вошли в терминологию отечественных культурологов. Однако 
        далеко не всегда при анализе конкретных явлений исследователи демонстрируют 
        нам именно <strong><em>мир</em></strong>, именно <strong><em>культуру</em></strong> 
        как целостный феномен, в котором смеховое начало является доминирующим.<br>
        <br>
        Применительно к студенчеству – весьма специфической профессионально-возрастной 
        страте – указанные понятия, насколько нам известно, до недавнего времени 
        (см. наши работы [7,8]), не прилагались, хотя именно данная субкультура 
        в наибольшей степени заслуживает таких дефиниций, ибо вне соответственного 
        рассмотрения – сквозь призму комического – вообще не может быть понята. 
        Впрочем, какого бы аспекта «неофициальной» жизни студентов ни касались 
        фольклористы и этнографы, они волей-неволей сталкивались со стихией смеха 
        – то существующей подспудно, то вырывающейся на волю и внятно заявляющей 
        о себе (см. последние работы украинских [2,6,15] и российских авторов 
        [5,9,11,18]). <br>
        <br>
        По М. Бахтину, «все многообразные проявления и выражения народной смеховой 
        культуры можно по их характеру подразделить на три основных вида форм:<br>
        <br>
        1. <strong><em>Обрядово–зрелищные формы</em></strong> (празднество карнавального 
        типа, различные площадные смеховые действа и пр.).<br>
        <br>
        2. <strong><em>Словесные–смеховые</em></strong> (в том числе пародийные) 
        произведения разного рода: устные и письменные…<br>
        <br>
        3. <strong><em>Различные формы и жанры фамильярно–площадной речи</em></strong> 
        (ругательство, божба, клятва…). [1, с. 9 ].<br>
        <br>
        Все виды этих форм (а к ним надо еще добавить визуальные, а также то, 
        что сейчас называют «приколами», а раньше называли «розыгрышами», «проделками» 
        или «хохмами») щедро представлены в современной украинской студенческой 
        субкультуре, и каждая из них заслуживает монографии или диссертации, однако 
        в данном исследовании в силу ограниченного объема, мы лишь обозначим их 
        основные параметры и дадим краткую характеристику первой категории. <br>
        <br>
        Обрядово-зрелищные формы студенческой смеховой культуры включают в себя: 
        <br>
        <br>
        а) шуточные ритуалы (посвящение в студенты, прием в жильцы общежития, 
        окончание института, похороны Словаря, вызов шары, предэкзаменационные 
        гадания и апотропеические обряды); <br>
        <br>
        б) официально санкционированные праздники (День студента, День факультета) 
        и игры (КВН);<br>
        <br>
        в) официально несанкционированные праздники («Тысяча и одна ночь» или 
        «Экватор», Хеллоуин, День группы, Первое апреля и др.).<br>
        <br>
        Конечно, во многих вузах <strong>посвящение в студенты</strong> – сугубо 
        официальное мероприятие, с большей или меньшей долей пафоса и патетики. 
        Однако не везде. Так, в Харьковском национальном университете им. В.Н. 
        Каразина на радиофизическом факультете есть такой обряд: «Первокурсники 
        приходят в универ, собираются все вместе с преподавателями, зажигают факелы 
        и идут к роднику. Там они становятся на колени и хором произносят торжественную 
        клятву с тезисами типа: «Клянусь до пятого курса выучить дорогу в институт 
        и ФИО декана…». Преподаватели ставят студентам на лоб и щеки печати «Радиофизик». 
        Потом они идут так гулять по городу».<sup>1</sup> Здесь, как видим, есть 
        и «карнавальное равенство» и карнавальное нарушение приличий и социальных 
        норм, и, конечно, гротесковая пародийность официального «муроприятия». 
        <br>
        <br>
        Обряд <strong>«инициации» первокурсников, поселяющихся в общежитии </strong>(особенно 
        в комнаты, где есть старшекурсники) – неминуем и отличается большим разнообразием. 
        Во многих харьковских общежитиях есть обычай 1 сентября выливать воду 
        из окон самых высоких этажей на нарядных первокурсников – что впрямую 
        ассоциируется с водным крещением, восходящим к архаическим посвятительным 
        и очистительным ритуалам и имеющим параллели со славянскими (и не только) 
        обычаями (например, <em>Поливальним понеділком </em>в украинской пасхальной 
        традиции, когда хлопцы обливают девчат водою). <br>
        <br>
        Вообще обрядовость в наибольшей степени свойственна – по понятным причинам 
        – именно обитателям общежития. Неписанный закон для поступивших в институт 
        и поселившихся в общаге – «1-й курс должен выставляться», т.е. поставить 
        «могар» старшекурсникам, иначе могут быть проблемы. «Инициационный» обряд 
        посвящения в студенты в общежитии у «китов» (студентов КИТ-факультета 
        – ф-та компьютерных и информационных технологий Национального технического 
        университета «Харьковский политехнический институт») проходит в три этапа. 
        «Первый – первокурсник выпивает стакан водки с солью и перцем. Второй 
        – первокурсника 15 раз бьют мокрым полотенцем по спине старшаки (старшекурсники 
        – <em>М.К.</em>). Если выдержал первые 2 этапа, то 3-й этап – нужно покатать 
        на спине двух старшекурсников. И студенты верят, что таким образом перваку 
        передается везение и удача».<sup>2</sup> Обратим внимание как на сходство 
        этого ритуала с армейскими посвятительными обрядами, так и на магическую 
        мотивировку данного действа, отнюдь не воспринимаемого самими «испытуемыми» 
        как проявление «дедовщины» или издевательства. <br>
        <br>
        <strong>Похороны Словаря</strong> – ритуал, существующий в Харьковском 
        авиационном институте (и, насколько нам известно, и в некоторых других 
        вузах), – вариант общераспространенного обычая «кремации» конспектов после 
        успешной сдачи экзаменов. Участники этого ритуала описывают его так: «В 
        те давние времена, когда иностранный язык сдавался как государственный 
        экзамен, родилась традиция своеобразного прощания хаёвцев с этим предметом, 
        вернее, с его вечным спутником, с помощью которого так лихо переводились 
        знаменитые «тысячи» и тексты.<br>
        <br>
        Ритуал погребения совершался обычно вечером после сдачи экзамена. Похоронная 
        процессия с рыданиями выносила из общаги тело усопшего импортно-отечественного 
        словаря и следовала к месту захоронения, находившемуся, как правило, у 
        тыльной стороны родного общежития. Произносились последние слова, тело 
        опускалось в предварительно выкопанную ямку глубиной около двух метров, 
        каждый бросал горсть земли, гулко бившейся о потрепанный, но еще твердый 
        переплет. Затем довершали свое дело могильщики. Надгробный холмик украшали 
        скромными цветами. Цветы быстро увядали, и вскоре о месте захоронения 
        забывали…<br>
        <br>
        Иногда кто-нибудь, кто после очередной попытки рассчитаться с библиотекой 
        с ужасом вспоминал чей это был словарь, бросался искать заветное место 
        на кладбище словарей, но – увы… Природа неумолима, а за традиции надо 
        платить.» [4, с. 67].<br>
        <br>
        <strong>Защита диплома</strong> у харьковских политехников, как и у «хаёвцев» 
        (студентов ХАИ), уже не одно десятилетие отмечается вечером в общежитии 
        «катанием на тазиках». Дипломника должны провезти (чаще всего это почетная 
        обязанность первокурсников, таким образом перенимающих «эстафету поколений») 
        по всему крылу здания с пятого по первый этаж. Сопровождается это действо 
        шутками и пожеланиями успехов в дальнейшей жизни. Уместная параллель – 
        <em>катання батьків</em> в украинской свадебной обрядности на второй или 
        третий день свадьбы (кстати, на Харьковщине их катают чаще всего в тачке, 
        а порой и в корыте); толкуется этот обряд самими участниками чаще всего 
        так: родители уже сделали свое дело (родили, воспитали, женили ребенка) 
        – <em>більш не нужні</em>! Однако это и выражение внимания к родителям, 
        карнавального почтения.<br>
        <br>
        В 1980-е годы в ХПИ <strong>получение лейтенантских погон</strong> ребята 
        отмечали, наряжая самое высокое дерево возле общежития военными галстуками.<sup>3</sup> 
        Разумеется, это шутейный вариант довольно распространенного обычая (в 
        частности, у туристов, а также кое-где в современной свадебной обрядности) 
        завязывания ленточек, платочков на дереве – «на счастье», «чтоб вернуться», 
        «чтоб не разлучаться» и т.п. <br>
        <br>
        <strong>Превентивная магия</strong>, в частности, <strong>ловля халявы 
        или шары</strong>, неоднократно становившаяся объектом внимания этнографов 
        [2, 3, 4, 7, 9], – весьма популярный обряд и в России, и в Украине. «Классический» 
        его вариант: в 12 часов ночи перед экзаменом надо высунуться в окно с 
        зачеткой в руке (часто это делают сразу несколько человек) и крикнуть: 
        «Халява, ловись!» или «Шара, приди!», затем закрыть зачетку (лучше всего 
        после этого перевязать ее красной ниткой) и раскрыть только на экзамене 
        (а то халява вылетит) – впрочем, ловят иногда спичечным коробком или пустой 
        пачкой из-под сигарет. Понятно, что этот «магический» ритуал производится 
        весьма весело, с шутками-прибаутками, подначками друг друга и т.п. Приведем 
        еще два его варианта. «Рано утром перед экзаменом широко распахнуть окно 
        общежития и громко прокричать 3 раза: «Шара, приди!» Если проснутся соседи 
        (лучше – из дома напротив), то хорошая оценка обеспечена».<sup>4</sup> 
        «Студент становится перед зачеткой, делает умное лицо, протягивает кверху 
        руки и зовет шару: «Шара, приди!»<sup>5</sup><br>
        <br>
        В ряде городов есть «памятники Шаре» (в Харькове и Киеве они располагаются 
        на территории авиаинститутов, впрочем, известны они не только студентам 
        этих вузов). В известное время к ним приходят студенты (порой с «жертвоприношениями») 
        и молят Шару об удаче (некоторые считают обязательным потереть зачеткой 
        о Шару). Включение городских памятников в предэкзаменационную обрядность 
        отмечено в Полтаве [3, с. 121] и некоторых других городах Украины.<br>
        <br>
        В наше время тотального наступления мобильной связи (кажется, уже не осталось 
        студента, даже «бедного», у которого не было бы мобилки) ловля шары может 
        производиться и с помощью этого «чуда техники». Первокурсник факультета 
        автоматики и приборостроения ХПИ Андрей Ванин рассказывает: «Прислали 
        мне недавно SMS, в котором было написано много разного и в конце написано, 
        что надо отправить это четырем своим друзьям из института – и жди халяву, 
        а если ты ее проигнорируешь, то завалишь сессию»<sup>6</sup>. Как это 
        похоже на известные «Письма счастья» или «Святые письма» (см. о них [10, 
        14, 16, 17]), которые доводилось получать и автору этих строк (последний 
        раз – в 2004 г. по Интернету, с настоятельным предложением переслать его 
        (а в былые годы – сначала переписать) энному количеству людей (кажется, 
        не меньше 40) – только в случае пренебрежения предложением обещались более 
        серьезные последствия: болезни, крах карьеры, а то и смерть. Возможно, 
        данная SMS-ка – просто розыгрыш, но не исключено, что отправитель его 
        в какой-то мере верил в «магический» эффект данного послания.<br>
        <br>
        Предэкзаменационные «магические» действия, в сущности, относятся к смеховой 
        культуре, поскольку многие студенты их совершают «прикола ради» (хотя 
        есть люди, которые на полном серьезе выполняют нехитрые «профилактические» 
        действия, «обеспечивающие» успешную сдачу экзамена или зачета). Широко 
        известен обычай подкладывать под левую пятку, идя на экзамен, 5 копеек, 
        но некоторые студенты ХПИ в полночь варили в молоке 5-копеечную монету, 
        причем на огне свечи.<sup>7</sup><br>
        <br>
        <strong>Гадания</strong> (как святочные, так и предэкзаменационные) все 
        еще остаются популярными среди женской половины студенчества. Вот, например, 
        как гадают в Новой Каховке. «Самый действенный способ узнать ответы на 
        все вопросы – это гадание с помощью зеркал. В пустой комнате установите 
        друг против друга два зеркала так, чтобы они отражались одно в другом. 
        Как только возникнет тринадцатое отражение, прочитайте вслух слова:<br>
        <br>
        Духи, слушайте меня! <br>
        Одеяло – простыня! <br>
        Выпив воду всю из крана, <br>
        Позовите дух декана!<br>
        <br>
        При этом в тринадцатом отражении появится декан. Вы должны сразу же задать 
        ему свой вопрос! Внимательно следите за тем, чтобы он к вам не приближался. 
        Если же он все же начнет двигаться к вам, заклинайте его словами: «Чур 
        меня от декана! Мать Алгебра, спаси и сохрани!» Иначе декан может выйти 
        из зеркала – и тогда быть беде.»<sup>8</sup> <br>
        <br>
        Не будем подробно останавливаться на официально <strong>санкционированных 
        праздниках и играх</strong>, хотя они тоже – мощное поле проявления студенческой 
        смеховой культуры. Симптоматично, что КВНы, не теряющие своей популярности 
        с 1960-х годов, не переставая оставаться субкультурным явлением, стали 
        (благодаря телевидению) в СССР и на постсоветском пространстве феноменом 
        общенародной популярной культуры, и образ студента как ВЕСЕЛОГО и НАХОДЧИВОГО 
        стал с тех пор просто хрестоматийным. <br>
        <br>
        «День факультета» во многих вузах – это повод для актуализации всех <em><strong>смехотворных</strong></em> 
        возможностей студентов – каждый факультет, каждая группа старается отчебучить 
        «что-нибудь эдакое», а если это невозможно (например, в силу строгости 
        декана) на самом мероприятии в стенах института или ДК, то уж непременно 
        на неофициальном продолжении бьющая через край «смеховая энергия» найдет 
        выход. В ХПИ на АП-факультете к этому дню приурочивается конкурс стенгазет 
        – и побеждает самая веселая, остроумная, незаурядная группа. В ХНУ им. 
        В.Н. Каразина обычно выпускается одна, но огромная стенгазета факультета 
        с веселыми рисунками, фотографиями, текстами. Венцом праздника является 
        традиционный «капустник». Особо знаменит в Харькове своими «днями» факультет 
        летательных аппаратов Харьковского авиационного института (описание этого 
        праздника см.: [4, с. 43-45]). В этом уникальном институте (известном, 
        кстати, и своими кавээнщиками), отмечаются также «День маразма» и «День 
        коней», выросшие из – соответственно – Дня самолетостроительного факультета 
        и Дня 5-го факультета. Приведем небольшой фрагмент описания того, что 
        бывает на «Дне коней»: «Кладбище любимых людей. &lt;…&gt; Несрочные похороны 
        ненавистных и любимых преподавателей, зам. деканов, друзей и лично заказчика 
        проводятся вбиванием между бетонными плитами, автоматически превращающимися 
        в надгробные, колышков с табличками у легендарного СУ-7Б, превращенного 
        тут же в объемную картину. «Тук, тук, тук» – и родственники с близкими 
        друзьями безвременно усопшего, чье ФИО с пожеланиями указано на табличке, 
        со слезами скорби бросаются возлагать цветы, венки и дрожащими голосами 
        производят отпевание. Бывало, что одного хоронили несколько раз и даже 
        рядом.<br>
        <br>
        Уилдебилконский теннисный турнир. Зрелище, претендующее на право самого 
        популярного. Отборочные игры в пинг-понг проводятся при содействии фирмы 
        «Сделано в СССР», любезно предоставляющей игроку вместо неинтересных ракеток 
        кастрюли обыкновенные общежитские. В остальном – все по мировым стандартам.<br>
        <br>
        Публичный дом. Здесь посетителям предлагаются в широком ассортименте курсовые 
        проекты, лабы, дипломы. В соседней комнате особо озабоченные могут посмотреть 
        в замочную скважину эротический фильм «Эммануэль». Менее озабоченные могут 
        понаблюдать за зрителями. А беззаботные могут посмотреть на наблюдателей.» 
        [4, с. 49-50]. Обратим внимание на столь характерное и для народной смеховой 
        культуры гротескное обыгрывание темы смерти (вспомним гуцульские «игры» 
        с телом покойника – «оживание» мертвеца, пародийные похороны во время 
        свадебных игр на Западной Украине, зафиксированные Г. Танцюрой, не говоря 
        уж о «смерти» и «воскрешении» Козы). <br>
        <br>
        Празднование <strong>«Экватора»</strong> (середины обучения) или <strong>«1000 
        и 1 ночи»</strong> (отмечается в ночь с 28 на 29 мая, а в случае «захвата» 
        високосного года – в ночь с 27 на 28 мая) для студентов первых трех курсов 
        овеяно легендами. Ходят самые невероятные слухи об этом празднике – ну, 
        например: староста должна в эту ночь станцевать стриптиз… Как бы там ни 
        было, после этой ночи на многих домах, заборах, памятниках появится загадочная 
        (для непосвященных) цифра «1001», нанесенная чаще всего краской… Наиболее 
        фундаментально отмечается этот праздник в ХАИ. «Во-первых, верстается 
        диплом и флаг. Диплом «1001» включает в себя титульный лист с фамилией 
        владельца, решением экзаменационной (аттестационной) комиссии, часто именуемой 
        просто шаровой, подписями последней, и, обязательно, печатью любой формы 
        с надписью «круглая». Далее – листы с торжественной клятвой и памяткой 
        владельцу…&lt;…&gt; Итак, если у вас все готово, то осталось немного: 
        выбрать квартирьеров, затариться, удачно посетить пункт проката и пригласить 
        женский пол. Ну вот, подготовка закончена, и в бой идут пока одни квартирьеры. 
        Электричка или автобус доставят их на Эсхар, Старый Салтов или в Гинеевку, 
        где они, не торопясь, разобьют палатки, поставят флагшток, опустят в озеро 
        (реку) звенящие сетки и, если уж по полной программе, – приготовят трассу 
        мужества. Осталось дождаться основной состав и приглашенную делегацию. 
        Возможна торжественная встреча, а затем гибкая программа с прохождением 
        трассы мужества, заседанием шаровой комиссии, зачитыванием оценок, характеристик, 
        вручением дипломов и собственно ночью, той самой, о необходимости которой 
        так долго говорили хаёвцы и которая, наконец, свершилась!<br>
