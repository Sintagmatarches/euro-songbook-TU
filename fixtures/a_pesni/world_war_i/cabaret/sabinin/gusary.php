<strong>Владимир Александрович Сабинин</strong> (1885-1930) - оперный певец, автор-исполнитель песен. <br>
<br>
Песня-марш про гусар опубликована в 1915 году. Ее фрагмент звучит в фильме Леонида Лукова &quot;Две жизни&quot; (1961). Время действия - лето 1917 года, Петроград. Ткачиха Нюша бегает по городу, пытаясь выяснить судьбу арестованного жениха-большевика, в это время на заднем плане марширует отряд солдат и поет эту песню.<br>
<br>
После Февральской революции Николай Фалеев (псевдоним - Чуж-Чуженин) написал и опубликовал <a href="../../ww1/nasolnce1917.php">новый текст</a> - о продолжении германской войны в условиях республики под красным знаменем. Мотив был использован в шуточной песне <a href="../../dvor/cilindr.php">&quot;Четыре зуба&quot;</a> (&quot;Цилиндром на солнце сверкая...&quot;) и в песне 1920-х годов "Да здравствует Первое мая". <br>
