	<span class="header"><strong>Boże, coś Rosyę<br>
  (Боже, что Россию)</strong><br></span>
  <br>
  Boże, coś Rosyę przez tak liczne wieki, <br>
  Trzymał w ciemnościach na hańbę ludzkości, <br>
  Coś jej odmawiał dotęd Twej opieki. <br>
  Robiąc narzędziem tyraństwa i złości.<br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Lud nasz nieszczęsny racz oświecić Panie!<br>
  <br>
  Ty, coś niedawno tknięty jej spodleniem, <br>
  Zesłał nam mężów, co światła promienie <br>
  Rzucili na kraj i wolności tchnieniem <br>
  Przygotowali z kajdan uwolnienie.<br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Pestlów, Hercenów, daj nam więcej, Panie!<br>
  <br>
  Daj naszej Rosji poczucie wolności, <br>
  Niech w niej wytępia katowskie jaskinie, <br>
  Daj iej poznanie о ludów godności, <br>
  Niech z barbarzyństwem despotyzm zaginie.<br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Оjczyźnie naszej racz dać wolność, Panie! <br>
  <br>
  Boże, którego ramię sprawiedliwe, <br>
  Żelazne berła świata władców kruszy, <br>
  Zniszcz raz tyranów zamiary szkodliwe, <br>
  Obudź nadzieie w biednej naszej duszy.<br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Z tyrańskiej władzy, racz wybawić, Panie! <br>
  <br>
  Znów naród polski ze łzami boleści,<br>
  Krwią męczenników kreśli dziejów kartę. <br>
  Со ludom Rosji tyraństwa obwieści. <br>
  I zrzuci piętno hańby niezatarte. <br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Od nowej hańby zbaw nasz naród, Panie! <br>
  <br>
  Gdy cała Polska żałobą okryta, <br>
  Modlitwą, łzami о swą wolność prosi,<br>
  Sołdat krzyż łamie, morduje i chwyta <br>
  Lud śpiewający, do fortec unosi.<br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Tak strasznej zbrodni racz odpuścić, Panie! <br>
  <br>
  О, Polsko! Polsko! za twoje cierpienia. <br>
  Któremi dręczy rząd dzikiej srogości,<br>
  Lud Rosyi błaga Twego przebaczenia, <br>
  Boś Ty lest dźwignią i jego wolności. <br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Z podłych służalców оczyść Rosyę, Panie! <br>
  <br>
  Nie walczyć z Polską, ale iść jej śladem, <br>
  Uczyć się od niej godności i zgody, <br>
  Nie grzeszyć dzikim, zbójeckim napadem <br>
  Na ziemię Polską i na jej swobody. <br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Takiem uczuciem racz nas natchnąć, Panie!<br>
  <br>
  I myśmy ofiar ponieśli już krocie, <br>
  Podobnej Polsce ulegamy doli, <br>
  А żyjąc ciągle w hańbie i sromocie, <br>
  Dźwigamy jarzmo ohydnej niewoli.<br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Zbudź synów Rosyi, miłosierny Panie! <br>
  <br>
  Boże wszechmocny! niech ta krew przelana, <br>
  Со ją broń nasza z Polski wytoczyła, <br>
  Nie na nas spadnie, ale na tyrana,<br>
  Którego ręce krew tа pobroczyła. <br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Krwawe morderstwa Rosyi, przebacz, Panie! <br>
  <br>
  Lud nasz nie pragnie dla zdobyczy boju, <br>
  Nie chce już cudzej własności posiadać, <br>
  Pragnie swobody używać w spokoju,<br>
  Nie chce swych więzów innym ludom wkładać. <br>
  <br>
  Przed Twe ołtarze zanosim błaganie: <br>
  Z kajdan niewoli racz nas rozkuć, Panie! <br>
  <br>
  Lud Rosyi przeklnie wyrodnego syna, <br>
  Со krwią Polaków splami swoje dłonie, <br>
  Matki i żony odepchną Kaina, <br>
  Który zeszpeci zbrodnią swoje dłonie. <br>
  <br>
  Przed Twe ołtarze zanosim błaganie:<br>
  Wyrodnych synów, nie daj więcej, Panie! <br>
  <br>
  (Dziennik Literacki) <br>
  <em><br>
  Jeszcze Polska nie zginęła! Pieśni patryotyczne i narodowe. Zebrał 
  Francisek Barański. New York, N.Y., The Polish Book Importing Co., Inc., 1944. Nr. 3 a.</em><br>
 <br>
  <br>
  На основе гимна <a href="bozecos.php">&quot;Boże, coś Polskę&quot;</a> (там 
  же см. ноты), сложенного не позднее Январского восстания 1830-1831 гг. из текстов 
  двух более ранних гимнов: &quot;Pieśń narodowa za pomyślność Króla&quot; (sł. 
  A. Feliński, muz. J. N. Kaszewski, 1816) i &quot;Hymn do Boga о zachowaniu wolności&quot; 
  (sł. A. Gorecki, muz. K. Kurpiński, 1817), - и исполняемого на анонимную мелодию 
  религиозного песнопения XVIII века &quot;Bądź pozdrowiona Panienko Maryjo&quot;. 
  <br>
  <br>
  В тексте упоминаются Пестель и Герцен - соответственно, &quot;Boże, coś Rosyę&quot; 
  написана не ранее эмиграции Герцена в Европу (1847), где он и приобрел широкую 
  известность как борец за свободу.<br>
