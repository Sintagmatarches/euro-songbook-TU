	<br>Маугли<br>
	<br>Эта песня — легендарной были...<br>
	  Подымайся, песня, как орлица, -<br>
	  Крик орлиный в звонкий рокот струнный<br>
	  Претвори, мощна и широка!..<br>
	<br>В час недобрый наших окружили,<br>
	  Оторвали от родной границы,<br>
	  Смертный дождь обрушили чугунный,<br>
	  Тесной сталью сжали два полка...<br>
	<br>Как итти? В подсумках — нет патронов!<br>
	  Как итти? Остыла сталь орудий...<br>
	  Смерть незрячим молчаливым взором<br>
	  Горсти смелых глянула в лицо.<br>
	<br>Но вздымались гордые знамена -<br>
	  Но смыкались в тесном строе груди -<br>
	  И штыковым бешеным напором<br>
	  Раздробилось грозное кольцо!<br>
	<br>Бились день, и день второй, и третий -<br>
	  И отвага устали не знала.<br>
	  И заглох под серою шинелью<br>
	  Их пароль: «На родину... Вперед!»<br>
	<br>Им заря, пылая на рассвете,<br>
	  Путеводный факел зажигала,<br>
	  Чтоб нести засыпанный шрапнелью<br>
	  Небывалый, сказочный поход...<br>
	<br>Слава вам, кто пал на поле бранном!<br>
	  Вам, кому дано было пробиться!<br>
	  Не исчезнут славные скрижали, -<br>
	  Не померкнут ваши имена...<br>
