	<span class="header"><strong> БОЙ ПОД ЩУРИНОМ</strong></span><br>
	Песнь Отдельного батальона Гвардейского экипажа<br>
	<br>
	Слова Бориса Германа<br>
	<br>
	<em>На мотив: <a href="../army/oleg.php">«Как ныне сбирается вещий Олег...»</a><br>
Оркестр: « Укусила муха...»</em><br>
<br>
Война началась. Мы вернулись из шхер,<br>
Ушли корабли боевые, —<br>
И на море немцам был явлен пример,<br>
Как бьются Гвардейцы морские.<br>
<br>
Обидно: на яхтах остался народ —<br>
Красавцы, лихие ребята...<br>
И стали просить сухопутной поход:<br>
Матрос поддержал чтоб солдата.<br>
<br>
«На суше и прежде дрались моряки...»<br>
И Царь разрешил отправляться <br>
Нам дали на фронте две главных реки.<br>
На них стали с немцем мы драться.<br>
<br>
<em>Оркестр: «Так громче, музыка...»</em><br>
<br>
В окопы послали... Речь Саблин ведет:<br>
«У нас в батальоне нет трусов.<br>
За трон и за Родину каждый умрет»...<br>
Внимательно слушает Усов.<br>
<br>
Он принял команду в участке плохом <br>
(Ведь скучно сидеть в обороне),<br>
Впервые знакомится он с моряком <br>
В Гвардейском лихом батальоне.<br>
<br>
<em>Оркестр: «Так громче, музыка...»<br>
«Здорово, красавцы, стрелки-моряки!»<br>
«Здравия желаем, Ваше Превосходительство!»</em><br>
<br>
Наш Саблин ему объясненья дает <br>
На самые мелочи даже,<br>
И каждую ночь по окопам идет,<br>
За славу страшась экипажа.<br>
<br>
<em>Оркестр: «Так громче, музыка...»<br>
«Не оплошайте, братцы!»<br>
«Постараемся, Ваше Высокоблагородие!»</em><br>
<br>
Как соты, окопы на том берегу,<br>
Пред ними с ручьями болото,<br>
Сеть проволок цепких защита врагу, — <br>
Тяжелая будет работа!<br>
<br>
Но славному бою пришел свой черед...<br>
Без всякой почти подготовки <br>
Соседи-стрелки понеслися вперед,<br>
Подняв лишь повыше винтовки.<br>
<br>
И страшный под Щурином бой закипел: <br>
Как град полетели снаряды,<br>
Рой пуль пулеметных, как буря, свистел!<br>
И гибли стрелки ряд за рядом.<br>
<br>
Но все же три роты болото прошли <br>
И громко «ура» разнеслося,<br>
И то, что в передних окопах нашли, <br>
Стрелкам-удальцам все сдалося.<br>
<br>
<em>Оркестр: «Так громче, музыка...»</em><br>
<br>
Уж много окопов забрали стрелки <br>
И меньше их все остается,<br>
А к немцам подходят резерва полки...<br>
Со взводом пять рот уж дерется!<br>
<br>
И видно, что тяжко героям-стрелкам:<br>
Нет силы, а надобно драться...<br>
Тут Хвощинский шепчет своим молодцам: <br>
«На помощь попросимся, братцы».<br>
<br>
И лишь разрешили, — летят моряки.<br>
Такое «ура» раздалося,<br>
Что немцы, завидев морские штыки,<br>
Со злобой кричат: «сорвалося!»<br>
<br>
<em>Оркестр: «Так громче, музыка...»</em><br>
<br>
Гвардейским матросам не диво вода,<br>
В болоте они не замнутся.<br>
Их издали видно, да то не беда!<br>
Бегут во весь рост и не гнутся.<br>
<br>
Уж многих убили из роты второй,<br>
И в ротах других есть потери.<br>
Но Хвощинский храбрых ведет за собой,<br>
И мечутся немцы, как звери.<br>
<br>
С налета забрали матросы окоп,<br>
Колючие сети разрушив.<br>
В них с флангов стреляют, стреляют им в лоб,<br>
Да храбрости тем не затушишь!<br>
<br>
<em>Оркестр: «Так громче, музыка...»</em><br>
<br>
Упорно и злобно дерутся враги,<br>
Их больше, и выбить их трудно.<br>
Но старую славу хранят моряки <br>
И бьются без устали, чудно...<br>
<br>
Не выдержал немец напор, — побежал...<br>
Хоть был он намного сильнее.<br>
Победу над ним Батальон одержал,<br>
Взял пленных, редут и трофеи.<br>
<em><br>
Хор: «Так громче, музыка, играй победу,<br>
Мы Щурин взяли, и враг бежит...»</em><br>
<br>
Живыми не вышли б стрелки из кольца,<br>
А тут бьют врага рядом с нами:<br>
Мы колем и гоним его без конца...<br>
Так славой покрыли мы знамя!<br>
<br>
И громко нас славят соседи-стрелки <br>
И общий начальник наш Усов:<br>
«Лишь самые лучшие наши полки<br>
Из немцев так делали трусов»...<br>
<br>
<em>Оркестр: «Так громче, музыка...»<br>
«Покорно благодарим, Ваше Превосходительство!»</em><br>
<br>
Так Щурин был взят, и окончился бой.<br>
Уж шутят матросы, как дети:<br>
«Удачлив бой первый, наш Саблин Второй <br>
И ваш полк стрелков Царских Третий».<br>
<br>
<em>Хор: «Так громче, музыка...»</em><br>
<br>
Собрались все: Яша, Чигаев, Брискорн,<br>
Кузьмин, Мясоедов-Иванов, <br>
Кублицкий, Секерин, Ловягин и Керн,<br>
Штукенберг, Родионов, Богданов.<br>
<br>
Как много рассказов про доблесть стрелков,<br>
Про нашу поддержку лихую,<br>
Про раны, про смерть удалых моряков,<br>
Погибших за землю родную.<br>
<br>
Хоть раненых много, но бодрость у всех —<br>
Удачей, победой все пьяны,<br>
Не стонут. ..И Герман, молясь за успех,<br>
Всю ночь перевязывал раны.<br>
<br>
Потом все начальство нас стало хвалить<br>
И сам командир Экипажа.<br>
<em>«Спасибо, молодцы, за службу!»<br>
«Рады стараться, Ваше Императорское Высочество!» </em><br>
Сильней батальон свой мы будем любить,<br>
Еще себя немцам покажем.<br>
<br>
<em>Хор: «Так громче, музыка...»</em><br>
<br>
Уехал наш Саблин к Царю на доклад,<br>
<em>«Прощайте, братцы».<br>
«Счастливого пути, Ваше Высокоблагородие».</em><br>
И вскоре ответ получился —<br>
Что славе морской Государь очень рад,<br>
Уроном большим огорчился.<br>
<br>
Погибшим и слава, и вечный покой,<br>
Матросам в пехотном мундире.<br>
«Уверен, что также пойдете вы в бой <br>
При новом своем командире».<br>
<br>
Государю Императору, нашему Августейшему Главнокомандующему прокричим старое морское «Ура» (6 раз)<br>
<br>
<em>Хор и оркестр: «Так громче, музыка, играй победу,<br>
Мы Щурин взяли, и враг бежит...»</em><br><br>
