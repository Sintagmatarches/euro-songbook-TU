	Гвардейский флотский экипаж, единственная гвардейская часть, существовавшая в русском военно-морском флоте, принимал актив­ное участие в Первой мировой войне, в том числе в боевых действи­ях на суше.<br>
<br>
	В августе 1914 года были сформированы 1-й и 2-й отдельные батальоны Гвардейского экипажа, направленные на сухопутный фронт. 31 марта 1915 го­да они были объединены в один Отдельный батальон Гвардейского экипа­жа, участвовавший в боевых действиях на различных участках фронта до марта 1917 года. Особенно отличились гвардейские моряки 15 июля 1916 го­да в ходе Ковельской операции войск Юго-Западного фронта, ставшей про­должением знаменитого Луцкого (Брусиловского) прорыва. В этот день Отдельный батальон Гвардейского экипажа совместно с Л.-гв. 3-м Его Ве­личества стрелковым полком, преодолев болотистую местность, овладел позициями противника у деревни Щурин Луцкого уезда Волынской губер­нии и самой деревней<sup>1</sup>.<br>
<br>
	Текст песни «Бой под Щурином», предлагаемый вниманию читателей, хранится в фонде Гвардейского флотского экипажа в Российском государст­венном архиве военно-морского флота (РГА ВМФ. Ф. 935. Оп. 3. Д. 175. Л. 1—3. Копия Машинопись). Автор стихов — младший врач Гвардейского экипажа надворный советник Борис Константинович Герман (1877—?). Песня представляет интерес не только как образец песенного творчества пе­риода Первой мировой войны, но и, в первую очередь, как своеобразная стихотворная хроника конкретного события, очень точно отразившая мно­гие его подробности. Исходя из этого, автор публикации счел необходимым сопроводить ее комментарием.<br>
