	<span class="header"><strong>КАК У ВИСЛЫ</strong></span><br>
	<br>Сем. Цыганок-Темрюкский<br>
	<br>Как у Вислы было дело у реки,<br>
	  Подлетали к ней орлы-сибиряки,<br>
	  С приамурских, уссурийских все полей,<br>
	  Подлетали легче ветра, да и злей.<br>
	<br>Уж не крепкий и не мощный ли народ:<br>
	  Коли надо, глубину бы взяли вброд,<br>
	  Да наказ им дал полковник атаман -<br>
	  Не спугнуть чтоб ненароком вражий стан.<br>
	<br>Как у Вислы было дело, за рекой,<br>
	  Тихо шли все незнакомой стороной,<br>
	  И полезли, и нагрянули, как шквал:<br>
	  Сибиряк наш будто буря налетал.<br>
	<br>На помине и враги теперь легки,<br>
	  Да приняли их штыком сибиряки.<br>
	  И одуматься не дали, и присесть,<br>
	  Сколько взяли, изрубили, где учесть!<br>
	<br>Разудалый же сибирский наш народ.<br>
	  Под папахой дума светлая живет -<br>
	  И пошли они с истомой боевой<br>
	  Дальше, глубже — не вернулись на покой.<br>
	<br>Дома что?! - жена и дети подождут:<br>
	  Вражьей силе вот уж близится капут...<br>
	  Пораскажут нам сибирские стрелки,<br>
	  Как у Вислы было дело, у реки.<br>
