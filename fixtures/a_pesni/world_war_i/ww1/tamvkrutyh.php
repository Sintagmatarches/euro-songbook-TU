      <br>
      <strong>Бои в Карпатах 1914-1917 гг.</strong><br>
      <br>
      Германия объявила войну России 19 июля (1 августа) 1914 года в ответ на 
      объявление Россией всеобщей мобилизации (18 июля). <br>
      <br>
      Бои в Карпатах - против австрийцев, а затем против германцев - шли с августа 
      1914 г. до лета 1915 г., а затем с лета 1916 г. до лета 1917 г. К марту 1915 г. русские 
      овладели Галицией и взяли Перемышль, практически уничтожив австро-венгерскую 
      армию. При этом русская армия истратила почти все имеющиеся боеприпасы. 
      В апреле в Галицию перебросили германские войска и 19 апреля началось катастрофическое 
      &quot;Великое отступление&quot; русских. Летом 1916 г., во время Брусиловского 
      прорыва, русские вторично закрепились в Галиции. Через год, во время последнего 
      наступления русской армии (началось 18 июня 1917 г.), ей удалось взять Галич 
      и Калуш. 6 июля германцы контратаковали и продолжали наступать вплоть до 
      заключения Брестского мира (3 марта 1918).<br>
