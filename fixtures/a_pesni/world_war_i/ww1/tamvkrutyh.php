      <strong>ВАРИАНТ <br>
      <br>
      Из-за гop крутых Карпатских</strong><br>
      <br>
      Из-за rop крутых Карпатских <br>
      Пробирался наш отряд, <br>
      Пробирался темной ночью <br>
      Санитарный наш обоз. <br>
      Впереди идет повозка, <br>
      На повозке красный крест, <br>
      Из повозки слышны стоны: <br>
      — Скоро, скоро ли конец?<br>
      — Обождите, потерпите, — <br>
      Отвечает им сестра. <br>
      А сама едва-едва, <br>
      Вся измучена, больна.<br>
      — Скоро мы на стан приедем, <br>
      Напою и накормлю, <br>
      Перевязки всем поправлю, <br>
      Женам письма напишу. <br>
      Вот сестричка пишет, пишет, <br>
      А на сердце тяжело, <br>
      Муж ее давно убитый, <br>
      Сердце кровью облило. <br>
      — Что, солдатик, тебе надо <br>
      Или пить тебе подать? <br>
      — Нет, сестричка, не поможет, <br>
      Я уже начал помирать. <br>
