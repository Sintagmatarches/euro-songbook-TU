      В КРУТЫХ ГОРАХ КАРПАТОВ</strong><br></span>
      <br>
      Там, в крутых горах Карпатов,<br>
      Между гор крутых и скал<br>
      Пробирался ночью темной<br>
      Санитарный наш отряд.<br>
<br>Впереди идет повозка,<br>
      На повозке красный крест.<br>
      Из повозки слышны стоны:<br>
      «Боже, скоро ли конец?»<br>
<br>«Скоро, скоро», — отвечает<br>
      Милосердная сестра.<br>
      А сама едва шагает,<br>
      Утомительно бледна.<br>
<br>«Скоро мы на пункт приедем,<br>
      Напою и накормлю,<br>
      Перевязки всем поправлю,<br>
      Всем я письма напишу.<br>
      Перевязки всем поправлю,<br>
      Всем я письма напишу».<br>
<br>И сестрица письма пишет,<br>
      Ей на сердце тяжело,<br>
      Муж ее давно в могиле,<br>
      Сердце кровью облито.<br>
<br>В палатах стоны раздаются<br>
      У походного одра,<br>
      И к больному идет быстро<br>
      Милосердная сестра.<br>
<br>«Что тебе, голубчик, надо,<br>
      Или пить тебе подать?»<br>
      «Нет, сестричка, не поможешь,<br>
      Я уж начал остывать.<br>
      Нет, сестрица, не поможешь,<br>
      Я уж начал остывать.<br>
<br>Попрошу тебя, сестрица,<br>
      Помолиться пред крестом.<br>
      А когда свободна будешь,<br>
      Извести родных письмом.<br>
      А когда свободна будешь,<br>
      Извести родных письмом».<br>
<br>Последнее слово принимает<br>
      От солдатика она,<br>
      Глаза навеки закрывает<br>
      Милосердная сестра.<br>
