В стороне от родной деревушки <br>
Спи спокойно, убитый герой, <br>
Только ветер, качая деревьев верхушки, <br>
Нарушает твой вечный покой. <br>
<br>
Беззаветно и храбро сражался <br>
Ты с врагами за край свой родной, <br>
Но, сраженный немецкою пулей, <br>
Остался спать навеки в могиле сырой. <br>
<br>
Спи, пусть тихую песнь напевает <br>
Ветерок над могилой твоей, <br>
Спи, родимый, пусть сердце твое отдыхает <br>
От волнений пережитых дней! <br>
<br>
Спишь, касатик, и сердцем не чуешь, <br>
Что далеко в родимой избе <br>
Терпеливо тебя ожидают <br>
Мать-старуха с женою в тоске. <br>
<br>
А сынишка сквозь слезы бормочет: <br>
"Мамка, скоро ли тятя придет?" - <br>
"Скоро, милый, вот лето настанет, <br>
Бог победу нам скоро пошлет. <br>
<br>
Кончится грозная битва с врагами, <br>
Царь наградит тех, кто выдержал бой <br>
За веру, Отчизну, за правду святую, <br>
Тогда и родимый вернется домой!" <br>
<br>
Но вещее сердце тоскует недаром, <br>
Предчувствует, бедное, горе, беду. <br>
И молится женщина, тихо рыдая: <br>
"О, Господи Боже, спаси сироту!.." <br>
