	<span class="header"><strong>ВИЖУ Я, НА ПЕРЕХОДЕ </strong><br></span>
<br>
Вижу я, на переходе<br>
	  Неприступных гор Карпат<br>
	  Совершает переходы<br>
	  Русских соколов отряд.<br>
	  С песней громкою подходят,<br>
	  Продвигаются вперед,<br>
	  Над опасностью смеются,<br>
	  Что привычный наш народ.<br>
	  Вдруг вдали раздался шорох:<br>
	  Команда немцев тут и пли!<br>
	  Наши пушки призамолкли.<br>
	  Мы готовимся в штыки.<br>
	  Бой кипел помногу суток,<br>
	  А солдат не унывал.<br>
	  Он к победе все стремился<br>
	  И об мире тосковал.<br>
	  Я зайду на ту поляну,<br>
	  Где кровавый бой кипел,<br>
	  Я увижу там картину:<br>
	  Лужи крови, груды тел.<br>
	  Там лежал солдатик ранен,<br>
	  Тихо просит он воды,<br>
	  Истекает кровью бедный,<br>
	  Просит: «Братец, помоги!»<br>
	  Что же будем делать, братья?<br>
	  Плачут матери, отцы,<br>
	  Плачут жены, плачут дети,<br>
	  Не идут домой бойцы.<br>
	  Хорошо вам, буржуазы,<br>
	  Сыпать ласковы слова,<br>
	  Посидели бы в окопах,<br>
	  Испытали то, что я.<br>
	  Мы сидим в открытых ямах.<br>
	  Мелкий дождик порошит.<br>
	  Как засыплют пулеметы,<br>
	  Так, наверно, нельзя жить.<br>
	  Тут приходит приказанье:<br>
	  «Из окопов вылезай!»<br>
	  Только голову покажешь -<br>
	  Шарапнели зажужжат.<br>
	  Много шло там с нами братьев,<br>
	  Много крови пролилось.<br>
	  Зачем немецкое начальство<br>
	  У нас в Россеи завелось?<br>
	  Когда отдалим Варшаву,<br>
	  Был там немец-генерал.<br>
	  Он набил свои карманы,<br>
	  Не простился и удрал.<br>
	  А комендант к деньгам прельстился<br>
	  И без бою Ковну сдал.<br>
	  Раньше думали про Берлин,<br>
	  Теперь нечего мечтать:<br>
	  Надо думать о Варшаве,<br>
	  Как у немцев отобрать.<br>
<br>
<em>Текст записан т. Лукашовым со слов колхозника т. Кадникова Н.Н. в с. Хомутово, Иркутского района, Иркутской области, в 1937 г.<br>
	<br>В фольклорном архиве фольклорной секции Иркутского государственного музея имеется вариант этой песни, главным образом разрабатывающий мотивы окопной жизни:</p>
	<br>Хорошо тебе на воле<br>
	  Сыпать бисером слова.<br>
	  Посидел бы ты в окопах,<br>
	  Испытал бы то, что я.<br>
	  Посидел бы ты в окопах,<br>
	  Покормил голодных вшей.<br>
	  Похлебал бы ты холодных<br>
	  И пустых вонючих щей.<br>
	  Ты б не перся жирным брюхом,<br>
	  К нам в окопы не ходил.<br>
	  Только ты войны не нюхал:<br>
	  Ты в тылу, собака, был!<br>
	<br>Гуревич А.В. Фольклор Восточной Сибири. Иркутск: ОГИЗ, 1938. С. 16. Раздел «В годы империалистической войны», №46.</em><br>
