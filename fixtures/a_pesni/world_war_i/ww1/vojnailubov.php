	В. Мазуркевич<br>
<br>Не кори меня, голубка,<br>
	  За воинственный мой пыл!<br>
	  Я купил два полушубка,<br>
	  Шарф и варежки купил.<br>
	<br>На тебя, мою голубку,<br>
	  Я затратил сердца жар,<br>
	  А на теплую покупку -<br>
	  Стихотворный гонорар.<br>
	<br>Я послал мой дар в окопы,<br>
	  Для бойцов родной земли,<br>
	  Чтоб немецкие холопы<br>
	  Застудить их не могли.<br>
	<br>Дух бодрей, коль плоть согрета...<br>
	  Как охотно, бросив все,<br>
	  Я и сам перо поэта<br>
	  Променял бы на ружье;<br>
	<br>Сам забрался бы в траншеи,<br>
	  Сам пошел бы в смертный бой,<br>
	  И лупил врага по шее<br>
	  Поэтической рукой!<br>
	<br>Будем биться, если надо,<br>
	  Если родина зовет!<br>
	  За поход живым — награда,<br>
	  Мертвым — память и почет.<br>
	<br>Коль вернусь живым я с боя<br>
	  К обожаемой моей,<br>
	  Верю, милого героя<br>
	  Ты обнимешь горячей;<br>
	<br>Если ж мне дано судьбою<br>
	  Быть убитым на войне,<br>
	  Светлой, чистой и святою<br>
	  Будет память обо мне.<br>
	<br>Не грусти же! Грусть-отрава...<br>
	  Лучше в путь меня готовь!<br>
	  Прежде — родина и слава,<br>
	  А потом уже любовь!<br>
