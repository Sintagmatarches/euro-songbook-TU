 <br>
  <br>
  Танго появилось в Бухаресте в конце 1944 года. Фронтовой советский художник Георгий Храпак (1922-1974) написал стихотворное письмо своей любимой. После одного из концертов Петра Лещенко, выступавшего тогда перед советскими частями, Георгий передал ему текст. По воспоминаниям вдовы певца Веры Лещенко, было это в декабре 1944 года. Лещенко текст очень понравился, он свел художника с руководителем своего оркестра Жоржем Ипсиланти, тот помог отредактировать стихи и написал музыку. Песня вошла в репертуар Петра Лещенко, а также жены Жоржа Ипсиланти Мии Побер. Ипсиланти отпечатал ноты и текст песни с ее портретом. <br>
  <br>
  Мия Побер в сопровождении оркестра Жоржа Ипсиланти  в 1945 году записала песню на пластинку (Бухарест, фирма &quot;Electrecord&quot;, фонограмму можно послушать <a href="http://www.russian-records.com/details.php?image_id=9768" target="_blank">здесь</a>). Якобы в 1945-1948 годах на той же фирме в сопровождении гитары и аккоредона песню записал и Петр Лещенко, но матрица была уничтожена в связи с опалой певца. <br>
  <br>
  Подробнее  историю песни см. Г. Зиновьев <a href="../emigr/a-toska.php">&quot;Я иду не по нашей земле...&quot;</a> (2007-2009).<br>
  <br>
  Известны поддельные записи этой песни &quot;под Лещенко&quot; с голосом москвича 
  Николая Маркова, сделанные в начале 1950-х на &quot;ребрах&quot; (&quot;костях&quot;) 
  - самодельных пластинках из использованных рентгеновских снимков. Такие диски 
  гуляли в СССР на барахолках.<br>
  <br>
  Мелодия танго прочно вошла в блатной фольклор. На нее была написана популярнейшая 
  лагерная песня <a href="../dvor/nepetchalsia.htm">&quot;Не печалься, любимая&quot;</a> 
  и воровская <a href="../dvor/vetervrosche.php">&quot;Ветер в роще листвою шуршит...&quot;</a> 
  (в последней нет припева, а &quot;Не печалься, любимая&quot; сохранила и припев). 
  К ней очень близка мелодия песен <a href="../dvor/serjubka.php">&quot;Серая 
  юбка&quot;</a> и <a href="../dvor/devvsinberete.php">&quot;Девушка в синем берете&quot;</a> 
  - возможно влияние (&quot;Серая юбка&quot; по стилю более ранная песня - конца 
  1920-х).<br>
