  <br>
  Песня сложена в 1920-е годы на мотив, бытовавший в еврейской среде Восточной 
  Европы еще в XIX веке. Автор этой мелодии не установлен. На этот же мотив пелись 
  написанные вскоре после 1-й Мировой войны поэтом Нохемом Штернхеймом популярные 
  песни на идиш &quot;Дос рэдл&quot; и &quot;Ой, Гот, дрэй ибэр дос рэдл&quot;. 
  Автор текста &quot;Папирос&quot; (по крайней мере, текста на идиш) - актер из 
  Гродно Герман Яблоков (1903-1981), в 1924 эмигрировавший в США. По одним данным, 
  песня была в СССР популярна уже в годы НЭПа, по другим - получила распостранение 
  после гастролей в 1959 году американских исполнительниц на идиш сестер Берри. 
  Известна также под заглавием &quot;Песня еврейского сироты&quot;. Непонятно, 
  какой текст появился раньше - идиш или русский (по логике вещей, идиш). <br>
  <br>
  Подробнее см.: Дмитрий Якиревич <a href="moskva/a-3pesni.php">&quot;Три песни на одну 
  мелодию&quot;</a> (не позднее 2008) и Инна Найдис <a href="izr/a-cyplinna.php">&quot;Цыпленок 
  жареный&quot;</a> (2006).<br>
