  1. Купите папиросы</strong><br>
  <br>
  <em>Ещё один пример уличного фольклора времён нэпа. По популярности эта песня 
  не отставала от «Кирпичиков» и «Бубличков» (см. раздел одесских песен). Её с 
  огромным воодушевлением пели беспризорники на улицах, на перронах и в поездах. 
  Песня постепенно перекочевала в «одесский» уличный фольклор. Как и в случае 
  с «Бубличками», появился перевод на идиш, где в первой строке припева сохраняется 
  зачин по-русски: «Купите, койфте, койфте, папироссен!» Поскольку из беспризорников 
  и босяков формировалась во многом многотысячная армия «блатных», «Папиросы» 
  в уголовном мифе входили в репертуар уважаюшего себя уркагана. Ниже приводятся 
  классический уличный вариант и вариант местечковых евреев на русском языке. 
