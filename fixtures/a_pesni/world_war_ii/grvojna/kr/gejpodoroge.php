        <br>
        <strong>Маринов Александр Васильевич </strong>(1897-1951), до революции 
        служил солдатом в русской армии; в годы Гражданской воевал в 84 кавполку 
        Первой Конной, где вступил в РСДРП(б) и был переведен в политработники. 
        Частью его работы было сочинение стихов и песен. Традиция приписывает 
        ему авторство двух распространенных песен: «Гей, по дороге» и <a href="vdolpofrontu.php">«Вдоль 
        по фронту кавалерия идет»</a>; обе сложены на популярные народные мелодии.<br>
