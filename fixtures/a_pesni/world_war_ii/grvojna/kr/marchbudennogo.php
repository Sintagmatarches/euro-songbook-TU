  <br>
  1920 <br>
  <br>
  <em>Неизвестный источник </em><br>
  <br>
  <br>
        Российская ассоциация пролетарских музыкантов (РАПМ) призывала на грани 1920-30-х гг.
        запретить &quot;Марш Буденного&quot; на том основании, что мелодия 
        это якобы традиционная еврейская. Запрета, конечно, не последовало, а РАМП, как и все прочие музыкальные объединения, в 1932 г. в результате создания Союза советских композиторов упразднили.<br>
  <br>
  Из статьи Н. Брюсовой &quot;На борьбу с музыкальным дурманом&quot;, журнал &quot;За пролетарскую музыку&quot;. 1930. №1. Приводится по изд.: Довести до конца борьбу с нэпманской музыкой. 2-й сб. статей. М.: Гос. муз. издат., 1931. (Массовая б-ка Всероссийской ассоциации пролетарских музыкантов). С. 27:<br>
  <br>
  <em>Справедливо отнестись к этой лже музыке так же, как мы относимся ко всем видам вредительства. Чем оно скрытнее, чем искуснее замаскировано, тем страшнее и вредоноснее. Оно глубоко проникло в рабочие и крестьянские массы. С увлечением поют &quot;Шахту №3&quot;, &quot;Кирпичики&quot;. Больше того - поют &quot;Смело мы в бой пойдем&quot; на мотив &quot;Белой акации&quot;, &quot;Марш Буденного&quot; на мотив обрядовой свыдебной песни, &quot;Коммунистов семья&quot; на мотив &quot;Хуторка&quot;. Разлагающую музыку присоединают к революционным словам.<br>
  <br>
  Надо, чтобы рабочие и крестьянские массы восстали против этйо своеобразной попытки классового врага внести разложение такими хитрыми и скрытыми способами.</em><br>
  <br>  <br>
        Здесь дана поздняя устоявшаяся редакция текста песни. СССР был образован 
        в декабре 1922 г., а песня, по официальной советской версии, появилась 
        в 1920 г. В то время петь могли лишь об &quot;РСФСР&quot;. Корректировка 
        текста должна была произойти в 1923-27 гг. - почти наверняка после 1925 
        г., когда Ворошилов был поставлен наркомом по военным и морским делам. 
        В поэме Маяковского &quot;Хорошо!&quot; (1927) цитируется уже фрагмент 
        этой песни с Ворошиловым и СССР. Вставка <span class='i'>&quot;Ведь с нами Ворошилов, /Первый красный офицер&quot;</span> даже по форме не совпадает с соответствующими строками 1-го и 3-го куплетов: здесь должны быть парно рифмующиеся строки с дактилической рифмой, т.е. последнее ударение падает на третий с конца слог: <span class='i'>&quot;я&#x301;сные - нена&#x301;стные&quot; .</span>Точно так ж выбивается из формы строка &quot;Даешь Варшаву! Дай Берлин!&quot;<br>
  <br>
  Первая строчка также встречается в виде <em>&quot;Мы - красная кавалерия&quot;</em>, 
  а первая строчка припева: <em>&quot;Веди ж, Буденный нас смелее в бой!&quot; 
  </em> (см.: Русские советские песни (1917-1977). Сост. Н. Крюков и Я. Шведов. 
  М., «Худож. лит.», 1977). Бывают и иные разночтнения - <em>&quot;Сумеет постоять 
  за СССР&quot; </em>вместо <em>&quot;Сумеем кровь пролить за СССР&quot;</em> 
  и др.<br>
  <br>
        <br>
        Официальная советская версия выглядит так: Дмитрий Яковлевич Покрасс (1899-1978) 
        и Анатолий Адольфович Д’Актиль (Френкель) (1890-1942) работали в белом 
        Ростове-на-Дону в эстрадном театре «Кривой Джимми». В январе 1920 г. город 
        был взят красными и Дмитрий Покрасс стал штатным композитором Первой Конной 
        Армии. Упоминаемые в тексте песни польская и крымская кампании были в 
        том же 1920 г. Первая публикация песни – в 1922 г. <br>
  <br>
        Если верить русской эмигрантской прессе (газета «Новое русское слово», 
        США, 6 и 14 декабря 1974 г.), Дмитрий Покрасс причастен также к созданию 
        белогвардейского <a href="../bel/marchdrozd.php">&quot;Марша дроздовцев&quot;</a> 
        (это произошло на полгода ранее).<br>
