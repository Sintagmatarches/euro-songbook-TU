  <em>&lt;фольклорная запись 1926 года&gt; <br>
  <br>
  Фольклоризированный вариант популярной пушкинской песни-баллады, в основе которой 
  раннее лицейское стихотворение «Романс» (1814). Это стихотворение было положено 
  на музыку еще при жизни поэта несколькими композиторами, в том числе Николаем 
  Сергеевичем Титовым (1829). Народный напев близок к мелодии Титова и к «жестоким» 
  романсам начала XX века. Песня-баллада, записанная в 1926 году в живом бытовании, 
  восходит к текстам, публиковавшимся в многочисленных песенниках еще в XIX веке, 
  а также в лубочных изданиях. <br>
  <br>
Антология русской песни / Сост., предисл. и коммент. Виктора 
  Калугина. -М.: Изд-во Эксмо, 2005.<br></em>
   <br>
  <br>
   Впервые стихотворение напечатано, без разрешения Пушкина, 
  в альманахе &quot;Памятник отечественных муз&quot; на 1827 год. Приобрело широкую популярность в разных слоях общества. Лубочные картины на сюжет &quot;Романса&quot; издавались 
  с 1830-х, стихотворение прочно вошло в дешевые песенники, а мелодия песни исполнялась шарманщиками 
  до тех пор, пока не исчезли сами шарманщики (примерно, 1930-е). Кроме Титова, 
  есть музыкальные версии Н.П. Де-Витте (1829) и неизвестного композитора (&quot;Музыкальный 
  альбом на 1832 год&quot;, СПб., 1831).<br>
