	<span class="header"><strong>ДЛИННЫЙ ПУТЬ "BELLA CIAO": ОТ ПЕСНИ ИДИШ ДО ПАРТИЗАНСКОГО ГИМНА</strong></span><br>
	<br>
	<em><a href="http://www.inosmi.ru/panorama/20080507/241207.html" target="_blank">InoСМИ.Ru</a>, 7.05.2008. Перевод статьи: Jenner Meletti. <a href="a-bellameletti.php">Da ballata yiddish a inno partigiano
il lungo viaggio di Bella ciao</a> // La Repubblica</a>, 12.04.2008. Перевод пользователя InoСМИ Italia.</em><br>
	<br>
	<br>
	<strong>Настоящую историю партизанской песни "Bella ciao" переписывали много раз. Любимой песней партизан была "Fischia il vento". Но она была слишком "коммунистической"</strong><br>
	<br>
	В конце концов, раскрытие секрета обошлось в два евро. &quot;В июне 2006 года я был в Латинском квартале Парижа в магазинчике дисков. Вижу один компакт-диск с названием: &quot;Klezmer - Yiddish swing music&quot;, двадцать композиций в исполнении различных оркестров. Покупаю CD и плачу эти два евро.<br>
	<br>Прошло несколько недель, еду на работу в машине и слушаю этот диск. Вдруг, сам не замечая, начинаю подпевать &quot;Una mattina mi son svegliato / o bella ciao, bella ciao, bella ciao, ciao, ciao...&quot;. Короче, это была музыка партизанской песни &quot;Bella ciao&quot;. Останавливаюсь, читаю название и исполнителя. Написано: &quot;Koilen (3'.30) - Mishka Ziganoff, 1919&quot;.<br>
	<br>И тогда началось мое путешествие в мир идиш и музыки клезмер. Я хотел узнать, как еврейская народная мелодия, рожденная в Восточной Европе, а затем эмигрировавшая в Соединенные Штаты в начале двадцатого века, стала основой гимна итальянских партизан.<br>
	<br>&quot;Настоящую историю &quot;Bella ciao&quot; переписывали много раз. Но Фаусто Джиованнарди (Fausto Giovannardi), инженер из Борго Сан Лоренцо и случайный турист в Париже, обнаружил важное звено: уже в 1919 году песенный припев играли и записали в Нью-Йорке. &quot;Как он потом попал в Италию, - говорит инженер, - нам узнать не дано. Может быть, его привез с собой итальянский эмигрант, вернувшийся из Соединенных Штатов.<br>
	<br>И с этим диском, копией диска 1919 года, я начал расследование.<br>
	<br>
	Ценную помощь мне оказали многие английские и американские профессора. Мартин Шварц (Martin Schwartz) из Калифорнийского университета в Беркли объяснил мне, что мелодия &quot;Kolein&quot; имеет выраженное русское звучание, и, наверное, произошла от народной песни идиш. Род Гамильтон (Rod Hamilton) из лондонской Британской библиотеки утверждает, что Мишка Цыганов был евреем из Восточной Европы, возможно, русским, а песня &quot;Koilen&quot; - это вариант песни на идиш &quot;Dus Zekele Koilen&quot;, что означает 'Маленькая сумка для угля', и имеется, по крайней мере, две записи этой песни, одна 1921 года в исполнении Абрахама Московица, и одна 1922 года в исполнении Морриса Голдштейна.<br>
	<br>
	От Корнелиуса ван Слидрегта (Cornelius Van Sliedregt), музыканта голландской группы &quot;KLZMR band&quot;, я получил подтверждение, что &quot;Koilen&quot; (а также koilin, koyln или koylyn) была записана Мишкой Цыгановым (Mishka Ziganoff, а также Tziganoff или Tsiganoff) в октябре 1919 года в Нью-Йорке. Он говорит, что это произведение основано на песне на идиш с полным названием &quot;The little bag of coal&quot;, &quot;Маленькая сумка с углем&quot;.<br>
	<br>Больше года работы. &quot;Maxwell Street Klezmer Band&quot; из Harvard Terrace, в Соединенных Штатах имеет в своем репертуаре &quot;Koylin&quot;, поэтому найти ноты было легко. Пробую сыграть мелодию... Это и есть &quot;Koilen&quot; Мишки Цыганова. Но остается сомнение. Как может быть евреем тот, кого зовут Цыганов? Ответ получаю от Эрни Грюнера (Ernie Gruner), руководителя австралийской группы Klezmer: Мишка Цыганов был &quot;Cristian gypsy accordionist&quot;, крещеным цыганом - аккордеонистом, который родился в Одессе, а затем открыл ресторан в Нью-Йорке: он хорошо говорил на идиш и работал музыкантом-клезмер&quot;.<br>
	<br>В конце концов, история &quot;Bella ciao&quot; всегда была непростой. Песня стала &quot;официальным&quot; гимном движения Сопротивления только спустя двадцать лет после войны.<br>
	<br>
	&quot;До 1945 года ее пели лишь некоторые группы партизан в районе Модены и вокруг Болоньи, - говорит Лучано Граноцци (Luciano Granozzi), профессор современной истории университета Катании. - Любимой песней партизан была &quot;Fischia il vento&quot; ('Дует ветер'). Но она была слишком &quot;коммунистической&quot;. Прежде всего, она была &quot;пересажена&quot; на советскую песню &quot;Катюша&quot; 1938 года. И слова не годились, так как не оставляли сомнений. &quot;Дует ветер, / свирепствует буря, / ботинки рваные, но нужно идти вперед: / завоевывать красную весну, / где встает солнце будущего&quot;. И в то время как правительство становится лево-центристским, &quot;Bella ciao&quot; заменяет &quot;Fischia il vento&quot;. Она была политически корректной, а ее отсылка на &quot;invasor&quot; (захватчик, первые строчки песни &quot;Bella ciao&quot; дословно: &quot;Однажды утром я проснулся и увидел захватчика&quot; - прим. пер.) подходила не только для Социалистической партии Италии, но также и для христианских демократов, и даже для Вооруженных сил.<br>
