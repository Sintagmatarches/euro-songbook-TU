	<span class="header"><strong>Da ballata yiddish a inno partigiano
il lungo viaggio di Bella ciao</strong></span>
	<br>
	<br>
	<em><a href="http://www.repubblica.it/2008/04/sezioni/spettacoli_e_cultura/ballata-bella/ballata-bella/ballata-bella.html" target="_blank">La Repubblica</a> (Italia), 12.04.2008. <a href="a-bellameletti-rus.php">Русский перевод</a>. </em><br>
	<br>
	<br>
	<strong>Il brano fu portato in America da un musicista tzigano originario di Odessa
Ne esiste anche una versione operaia cantata dalle mondine dopo la guerra</strong><br>
	<br>
	BORGO SAN LORENZO - In fin dei conti, svelare un segreto è costato solo due euro. &quot;Nel giugno del 2006 ero al quartiere latino di Parigi, in un negozietto di dischi. Vedo un cd con il titolo: &quot;Klezmer - Yiddish swing music&quot;, venti brani di varie orchestre. Lo compro, pagando appunto due euro. Dopo qualche settimana lo ascolto, mentre vado a lavorare in macchina. E all'improvviso, senza accorgermene, mi metto a cantare &quot;Una mattina mi son svegliato / o bella ciao, bella ciao, bella ciao, ciao, ciao...&quot;. Insomma, la musica era proprio quella di Bella ciao, la canzone dei partigiani. Mi fermo, leggo il titolo e l'esecutore del pezzo. C'è scritto: &quot;Koilen (3'.30) - Mishka Ziganoff 1919&quot;. E allora ho cominciato il mio viaggio nel mondo yiddish e nella musica klezmer. Volevo sapere come una musica popolare ebraica nata nell'Europa dell'Est e poi emigrata negli Stati Uniti agli inizi del '900 fosse diventata la base dell'inno partigiano&quot;.<br>
	<br>E' stata scritta tante volte, la &quot;vera storia di Bella ciao&quot;. Ma Fausto Giovannardi, ingegnere a Borgo San Lorenzo e turista per caso a Parigi, ha scoperto un tassello importante: già nel 1919 il ritornello della canzone era suonato e inciso a New York. &quot;Come poi sia arrivato in Italia - dice l'ingegnere - non è dato sapere. Forse l'ha portato un emigrante italiano tornato dagli Stati Uniti. Con quel cd in mano, copia dell'incisione del 1919, mi sono dato da fare e ho trovato un aiuto prezioso da parte di tanti docenti inglesi e americani. Martin Schwartz dell'università della California a Berkeley mi ha spiegato che la melodia di Koilen ha un distinto suono russo ed è forse originata da una canzone folk yiddish. Rod Hamilton, della The British Library di Londra sostiene che Mishka Ziganoff era un ebreo originario dell'est Europa, probabilmente russo e la canzone Koilen è una versione della canzone yiddish &quot;Dus Zekele Koilen&quot;, una piccola borsa di carbone, di cui esistono almeno due registrazioni, una del 1921 di Abraham Moskowitz e una del 1922 di Morris Goldstein. Da Cornelius Van Sliedregt, musicista dell'olandese KLZMR band, ho la conferma che Koilen (ma anche koilin, koyln o koylyn) è stata registrata da Mishka Ziganoff (ma anche Tziganoff o Tsiganoff) nell'ottobre del 1919 a New York.<br>
	<br>Dice anche che è un pezzo basato su una canzone yiddish il cui titolo completo è &quot;the little bag of coal&quot;, la piccola borsa di carbone&quot;.<br>
	<br>Più di un anno di lavoro. &quot;La Maxwell Street Klezmer Band di Harvard Terrace, negli Stati Uniti, ha in repertorio &quot;Koylin&quot; e trovare lo spartito diventa semplice. Provo a suonare la melodia... E' proprio la Koilen di Mishka Tsiganoff. Ma resta un dubbio. Come può uno che si chiama Tsiganoff (tzigano) essere ebreo? La risposta arriva da Ernie Gruner, un australiano capobanda Klezmer: Mishka Tsiganoff era un &quot;Cristian gypsy accordionist&quot;, un fisarmonicista zingaro cristiano, nato a Odessa, che aprì un ristorante a New York: parlava correttamente l'yiddish e lavorava come musicista klezmer&quot;. Del resto, la storia di Bella ciao è sempre stata travagliata. La canzone diventa inno &quot;ufficiale&quot; della Resistenza solo vent'anni dopo la fine della guerra.<br>
	<br>
	&quot;Prima del '45 la cantavano - dice Luciano Granozzi, docente di Storia contemporanea all'università di Catania - solo alcuni gruppi di partigiani nel modenese e attorno a Bologna. La canzone più amata dai partigiani era &quot;Fischia il vento&quot;. Ma era troppo &quot;comunista&quot;. Innanzitutto era innestata sull'aria di una canzonetta sovietica del 1938, dedicata alla bella Katiuscia. E le parole non si prestavano ad equivoci. &quot;Fischia il vento / infuria la bufera / scarpe rotte e pur bisogna andar / a conquistare la rossa primavera / dove sorge il sol dell'avvenir&quot;. E così, mentre stanno iniziando i governi di centro sinistra, Bella ciao quasi cancella Fischia il vento. Era politicamente corretta e con il suo riferimento all'&quot;invasor&quot; andava bene non solo al Psi, ma anche alla Dc e persino alle Forze armate. Questa &quot;vittoria&quot; di Bella ciao è stata studiata bene da Cesare Bermani, autore di uno scritto pionieristico sul canto sociale in Italia, che ha parlato di &quot;invenzione di una tradizione&quot;. E poi, a consacrare il tutto, è arrivata Giovanna Daffini&quot;.<br>
	<br>La &quot;voce delle mondine&quot;, a Gualtieri di Reggio Emilia nel 1962 davanti al microfono di Gianni Bosio e Roberto Leydi aveva cantato una versione di Bella Ciao nella quale non si parlava di invasori e di partigiani, ma di una giornata di lavoro delle mondine. Aveva detto che l'aveva imparata nelle risaie di Vercelli e Novara, dove era mondariso prima della seconda guerra mondiale. &quot;Alla mattina, appena alzate / o bella ciao, bella ciao, ciao, ciao / alla mattina, appena alzate / là giù in risaia ci tocca andar&quot;. &quot;Ai ricercatori non parve vero - dice il professor Granozzi - di avere trovato l'anello di congiunzione fra un inno di lotta, espressione delle coscienza antifascista, e un precedente canto del lavoro proveniente dal mondo contadino.<br>
	<br>La consacrazione avviene nel 1964, quando il Nuovo Canzoniere Italiano presenta a Spoleto uno spettacolo dal titolo &quot;Bella ciao&quot;, in cui la canzone delle mondine apre il recital e quella dei partigiani lo chiude&quot;. I guai arrivano subito dopo. &quot;Nel maggio 1965 - cito sempre il lavoro di Cesare Bermani - in una lettera all'Unità Vasco Scansani, anche lui di Gualtieri, racconta che le parole di Bella ciao delle mondine le ha scritte lui, non prima della guerra, ma nel 1951, in una gara fra cori di mondariso, e che la Daffini gli ha chiesto le parole. I ricercatori tornano al lavoro e dicono che comunque tracce di Bella ciao si trovano anche prima della seconda guerra. Forse la musica era presente in qualche canzone delle mondine, ma non c'erano certo le parole cantate dalla Daffini, scritte quando i tedeschi invasor erano stati cacciati da un bel pezzo dall'Italia&quot;. &quot;Una mattina mi sono alzata...&quot;.<br>
	<br>Fino a quando ci sarà ricordo dei &quot;ribelli per amore&quot;, si alzeranno le note di Bella Ciao, diventato inno quando già da anni i partigiani avevano consegnato le armi. &quot;Bella Ciao? Forse le cantavano - dice William Michelini, gappista, presidente dell'Anpi di Bologna - quelli che erano in alta montagna. Noi gappisti di città e partigiani di pianura, gomito a gomito con fascisti e nazisti, non potevamo certo metterci a cantare&quot;.<br>
