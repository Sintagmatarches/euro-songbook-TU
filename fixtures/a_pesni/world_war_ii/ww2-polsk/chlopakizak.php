<strong>Historia realizacji piosenki</strong><br>
<br>

Dwa lata temu byłam na koncercie piosenki Powstańczej w Muzeum Powstania Warszawskiego, tam miałam okazje rozmawiać z panem Tymoteuszem Duchowskim ps "Motek" i od niego usłyszałam pierwszy raz o tej zapomnianej piosence. Rozmawialiśmy tez o tym, ze mało kto śpiewa tak jak to robiono dawniej, co dało mi do myślenia...<br>
<br>
Od kilku lat istnieje Grupa Teatralna "Warszawiaki", naszym celem jest właśnie wskrzeszanie tego co zapomniane! Pokazanie klimatu dawnej Warszawy... staramy się być alternatywą do nowoczesnych wykonań dawnych melodii i ich aktorskich interpretacji.<br>
<br>

Nie zastanawiając się długo obiecałam "Motkowi", że zrealizujemy tę piosenkę jak należy! Na skrawku papieru dostałam zapisany do niej tekst, mam tę karteczkę do dnia dzisiejszego. Z okazji 66. rocznicy wybuchu Powstania Warszawskiego piosenka jest już gotowa!<br>
