	  Na miasto spadła nie jedna paka.<br>
	  Już są granaty i rozpylacze,<br>
	  i wieją szkopy na widok AK.<br>
	<br>Na Saskiej Kępie wredne sowiety<br>
	  Co pomoc mieli dać dla Polaka,<br>
	  Ruskim zwyczajem nas wykiwali,<br>
