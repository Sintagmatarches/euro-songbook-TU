	<strong>Подстрочный перевод <br>
	<br>
	Хлопцы из АК </strong><br>
	<br>
	Первого августа по всей Варшаве<br>
	Началась огромная драка.<br>
	С одной стороны встали германцы,<br>
	С другой стороны — хлопцы из АК.<br>
	<br>У германцев были шкафы и коровы,<br>
	  А также помощь русского казака.<br>
	  Не было этого у бедной Варшавы,<br>
	  Но зато у ней были хлопцы из АК.<br>
	<br>Генерал Бур по радио Черчиллю:<br>
	  «Спасай, - говорит, - брата-поляка!»<br>
	  Ведь мы тут уже не выстоим,<br>
	  И сгинут все хлопцы из АК.<br>
	<br>И прилетели ночные десанты,<br>
	  На город упало много тюков.<br>
	  Вот уже есть гранаты и распылители,<br>
	  И сдувает фрицев при виде АК.<br>
	<br>На Саской Кемпе вредные Советы,<br>
	  Которые помощь обещали для поляка.<br>
	  По русскому обычаю нас обманули,<br>
	  Печальная доля у хлопцев из АК.<br>
	  <br>
	<br>
	<em>«Коровы» и «шкафы»</em> – прозвища, данные шестиствольным залповым мортирам Nebelwerfer-41 (калибр 15 см) и тяжелым ракетным установкам Schweres Wurfgestell (калибр 32 см). Ими немцы бомбардировали повстанческую Варшаву. Прозвище «коровы» использовали в варшавском районе Средместье, «шкафы» - в других районах. <br>
	<br>
	<em>«А также помощь русского казака»</em> - русские казачьи части Вермахта, брошенные в числе других коллаборационистских частей на подавление Варшавского восстания.<br>
	<br>
	<em>«Распылитель» (rozpylacz) - разг.</em> автоматический пистолет.<br>
<br>
	<br>
	<strong>История постановки песни</strong><br>
	<br>
Два года назад была на концерте повстанческой песни в Музее Варшавского восстания, там разговорилась с Тимотеушем Духовским, боевая кличка «Мотек», и от него впервые услышала об этой забытой песенке. Рассуждали также о том, что мало кто поет так, как это делали раньше, что заставило меня задуматься...<br>
	<br>
	Театральная группа «Варшавяне» существует несколько лет, и наша цель — именно воскрешение того, что забыто! Показ обстановки прежней Варшавы... стараемся быть альтернативой современным аранжировкам старых мелодий и их артистическим интерпретациям.<br>
	<br>Не долго думая, пообещала «Мотеку», что мы исполним эту песенку, как надо! На клочке бумаги получила написанный к ней текст, и храню эту бумажку до нынешнего дня. К 66-й годовщине начала Варшавского восстания песенка была уже готова!<br>
	<br>Хотя не было у нас спонсоров и медийных партнеров... удалось!<br>
