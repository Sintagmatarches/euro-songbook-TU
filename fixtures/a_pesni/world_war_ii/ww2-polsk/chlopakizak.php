	  się rozpoczęła ogromna draka.<br>
	  Po jednej stronie stali Germańce –<br>
	  po drugiej stronie chłopaki z AK.<br>
	<br>Germańce mieli szafy i krowy<br>
	  oraz ruskiego pomoc kozaka.<br>
	  Tego nie miała biedna Warszawa<br>
