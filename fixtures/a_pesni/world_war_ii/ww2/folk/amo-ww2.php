        <span class="text"> <span class="header"><strong>РАССКАЖУ ПРО ТОТ КРАЙ, ГДЕ БУШУЕТ...</strong></span><br>
        <br>
        Расскажу про тот край, где бушует,<br>
          Где машины заносит пурга,<br>
          Где балтийские ветры кочуют,<br>
          Где солдатская жизнь нелегка.<br>
        <br>В этом крае, разбитом войною,<br>
          Много ездило там шоферов,<br>
          Но был самый отчаянный шофер,<br>
          Звали Колька его Снегирев.<br>
        <br>Он машину — трехтонного ЗИС'а,<br>
          Словно брата родного любил,<br>
          От советской границы к Берлину<br>
          Все дороги на ней изучил.<br>
        <br>А на Студе работала Лелька,<br>
          Часто-часто вечерней порой<br>
          Студ могучий с улыбкою Лельки<br>
          Мимо Кольки промчится стрелой.<br>
        <br>Как-то раз Колька Лельке признался,<br>
          Но коварная Лелька была,<br>
          Посмотрела с улыбкой на Кольку<br>
          И по Студу рукой провела:<br>
        <br>«Милый Коля, ты парень что надо,<br>
          Но признаться тебе я должна:<br>
          Когда ЗИС Студебеккер догонит,<br>
          Тогда Лелечка будет твоя!»<br>
        <br>Как-то раз уж вечерней порою<br>
          С фронта ехал водитель домой.<br>
          Студ зеленый с улыбкою Лельки<br>
          Мимо Кольки промчался стрелой.<br>
        <br>Колька вздрогнул — и сердце забилось,<br>
          Бстро вспомнил он свой договор,<br>
          В ту минуту рванулась машина,<br>
          И запел свою песню мотор.<br>
        <br>Поворот — и машины сравнялись,<br>
          Колька Лельку в лицо увидал,<br>
          И забилося сердце у Кольки,<br>
          И на миг он забыл про штурвал...<br>
        <br>Полетела машина как птица,<br>
          Полетела прямо в овраг,<br>
          И последней мелькнула кабина,<br>
          Только слышен был голос: «Прощай!»<br>
        <br>А в овраге — обломки да камни,<br>
          И по камням бушует вода.<br>
          В том холодном и грязном овраге<br>
          Схоронил и его навсегда.<br>
        <br>И на память лихому шоферу,<br>
          Что машину как ветер гонял,<br>
          На могиле оставили фары<br>
          И от ЗИС'а погнутый штурвал.<br>
        <br>И не ездит теперь, как бывало,<br>
          Студебеккер над Вислой рекой.<br>
          Смотрит Лелька будто устало<br>
          И не держит штурвала рукой.<br>
        <br>
		<em>Пушкарев Л.Н. По дорогам войны. Воспоминания фольклориста-фронтовика. М: ИРИ РАН, 1995. С. 124-126. </em><br>
