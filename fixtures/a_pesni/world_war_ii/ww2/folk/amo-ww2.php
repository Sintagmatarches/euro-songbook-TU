		<br>
		Переделка песни на стихи Михаила Михеева <a href="../../profes/amo.php">про машину АМО</a> и Кольку Снегирева, сделанная во время Великой Отечественной войны Александром Козярским (род. в 1920 г. в Каменец-Подольской обл. УССР) - запевалой батальона химзащиты на 2-м Белорусском фронте. <br>
