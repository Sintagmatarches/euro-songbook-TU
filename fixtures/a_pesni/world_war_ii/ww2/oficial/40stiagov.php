  <strong>ПУТЬ ДОМОЙ<br>
  <br>
  - Что было со Знаменем Победы после окончания войны?</strong><br>
  <br>
  - В радостях победных майских дней 1945-го о нем как бы забыли. <br>
  <br>
  Даже в приказе Верховного Главнокомандующего о взятии нашими войсками Берлина, 
  да и в последующих его приказах о нем не упоминается. В связи с передислокацией 
  соединений 3-й ударной армии на новое место командир 79-го стрелкового корпуса 
  генерал Переверткин приказал 9 мая снять Знамя Победы и вместо него поставить 
  большой алый стяг. Выполнили приказ те же Егоров и Кантария. На месте Знамени 
  Победы они закрепили другой красный флаг. На нем серп, молот и звезда располагались 
  в центре полотнища. Знамя Победы было передано на хранение в политотдел 150-й 
  стрелковой дивизии. Там по распоряжению начальника политотдела дивизии Артюхова 
  на полотнище была сделана надпись: "150 стр. Ордена Кутузова II ст. Идриц. Див". 
  За это самоуправство Артюхову крепко досталось от генерала Литвинова, члена 
  Военного совета армии. 19 июня 1945 года из Москвы пришел приказ доставить Знамя 
  Победы в столицу, где готовился Парад Победы. <br>
  <br>
  В тот же день на Знамени были сделаны добавления к уже имеющейся надписи: "79 
  Ск", а затем еще и "3 УА 1 БФ" (З-я Ударная армия, 1-й Белорусский фронт}. Так 
