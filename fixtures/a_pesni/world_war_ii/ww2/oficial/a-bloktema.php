	 Арлен Блюм </strong><br>
  <br>
  <span class="header"><strong>БЛОКАДНАЯ ТЕМА В ЦЕНЗУРНОЙ БЛОКАДЕ<br>
  По архивным документам Главлита СССР</strong><br></span> 
  <br>
  <em>Журнал «Нева». 2004. №1. С. 238-245.</em><br>
  <br>
   <br>
  Через пять лет после полного снятия блокады предпринята была широкомасштабная 
  цензурная акция: пересмотру и «зачистке», если применить современный зловещий 
  термин, подлежала вся книжная продукция Ленинграда послевоенного времени. Причины, 
  вызвавшие ее, вполне понятны. Ленинград, и ранее находившийся под подозрением, 
  окончательно попал в идеологический штрафбат. Издававшиеся здесь журналы «Звезда» 
  и «Ленинград» допустили публикацию «несвоевременных» и «чуждых нам» произведений, 
  что привело к известным августовским постановлениям ЦК 1946 года и погромному 
  докладу Жданова. И одна из последних «судорог» сталинского режима, - «Ленинградское 
  дело».<br>
  <br>
  По приказу идеологических структур обкома партии Леноблгорлит составил тогда 
  около десятка аннотированных проскрипционных списков, в которые вошло несколько 
  сот книг, подлежащих изъятию и уничтожению. В свою очередь обком посылал эти 
  списки в Управление пропаганды и агитации ЦК на предмет получения санкции. Сопровождались 
  они таким «предуведомлением»: «Ленинградский обком, рассмотрев предложение Леноблгорлита 
  об изъятии из библиотек Ленинграда и Ленинградской области книг, указанных в 
  прилагаемом списке... просит о его утверждении». (1) За редчайшими исключениями 
  ЦК «шел навстречу» таким пожеланиям. Мало того: эти книги подлежали <em>тотальному</em> 
  запрету, включаясь затем в особые главлитовские «Списки книг, подлежащие изъятию 
  из библиотек и книготорговой сети», рассылавшиеся по всей стране.<br>
  <br>
  Среди книг, указанных в «ленинградских списках», более двух десятков, по моим 
  подсчетам, относится непосредственно к блокадной тематике. Если блокада Ленинграда 
  продолжалась приблизительно два с половиной года, то многие книги о ней получили 
  гораздо больший срок заключения в библиотечных узилищах — спецхранах. Ряд таких 
  книг, арестованных на рубеже 1940-1950-х годов, освобожден был спустя 7-8 лет 
  — после реабилитации пострадавших по «ленинградскому делу», другие задержались 
  по разным причинам на три-четыре десятилетия, будучи освобожденными только под 
  самый занавес перестройки. Не нужно, однако, также забывать, что уцелели лишь 
  считанные экземпляры, сохранившиеся в крупнейших библиотеках, располагавших 
  отделами специальных фондов, получивших обиходное название «спецхранов»; в остальных 
  они были уничтожены «путем обращения в бумажную массу». <br>
  <br>
  Каковы же причины конфискации и уничтожения литературы, затрагивающей эту тему? 
  Можно выделить два основных мотива — «персонифицированный» и «содержательный». 
  Согласно первому, книга подверглась запрету исключительно за упоминание «нежелательных» 
  имен. В этом случае осуждалось само имя <em>как таковое</em>, в строй вступал 
  argumentum ad hominem — доказательство «применительно к человеку», не основанное 
  на объективных данных, что, кстати, признавалось несостоятельным еще в римском 
  праве. В глазах идеологического аппарата и цензуры тот или иной человек после 
  ареста становился «нелицом» и подлежал «распылению», если вспомнить термины, 
  применявшиеся чиновниками «Министерства правды» из романа Джорджа Оруэлла «1984». 
  В «лагерную пыль» человек превращался не только физически: его имя навсегда 
  должно было исчезнуть из народной памяти.<br>
  <br>
  От такого тотального подхода пострадала и литература, посвященная блокаде. Основными 
  «виновниками» ее уничтожения (не по своей, конечно, вине!) стали Алексей Александрович 
  Кузнецов (1905-1950) и Петр Сергеевич Попков (1903-1950), арестованные в 1949 
  году по так называемому «ленинградскому делу» и через год расстрелянные. Кузнецов 
  в 1938-1943 годах был вторым, а в 1944-1946 годах — первым секретарем обкома 
  партии. В связи с назначением Кузнецова в 1946 году секретарем ЦК ВКП(б) на 
  этом посту его сменил Попков, до того работавший председателем Ленгорисполкома. 
  После снятия его с поста 22 февраля 1949 года и объявления ему выговора, он, 
  дабы в городе не появились «излишние разговоры, «был направлен на «учебу» в 
  Москву, в аспирантуру Академии общественных наук, но вскоре арестован. (2) Оба 
  они, организаторы обороны Ленинграда, не покидавшие его, не раз выступали на 
  собраниях, городских общественных митингах по случаю освобождения города и т. 
  д. Естественно, эти «руководящие» материалы неоднократно цитировались или хотя 
  бы упоминались практически во всех книгах блокадной тематики. <br>
  <br>
  Так, например, оказались в спецхранах «Северные повести» ленинградского прозаика, 
  военного корреспондента в годы Отечественной войны Геннадия Семеновича Фиша, 
  дважды выпущенные Лениздатом в 1948 и 1949 годах. Единственная причина: «...на 
  с. 189 и 292 упоминается фамилия П. Попкова». (3)<br>
  <br>
  По той же причине запрещен роман поэта, прозаика и литературного критика В. 
  М. Саянова «Небо и земля», причем с такой тотальной пометкой в главлитовском 
  списке: «<em>Все издания</em>» (роман выдержал 5 изданий). Сам автор был в 1944-1946 
  годах главным редактором журнала «Звезда»; снят со своего поста после выхода 
  упомянутого выше идеологического постановления ЦК. По поводу одного из изданий 
  романа (Л.: Молодая гвардия, 1948) Леноблгорлит доносил: «На с. 500—501 передается 
  выступление на радиомитинге А. Кузнецова как секретаря Ленинградской парторганизации 
  в период блокады и обращение к ленинградцам Попкова. Данное издание переиздано 
  в 1949 г. с устранением указанных криминалов. Сохранение указанного издания 
  нецелесообразно». (4)<br>
  <br>
  Попал под нож ряд альманахов и сборников, содержащих произведения ленинградских 
  писателей, в частности, литературно-художественный сборник «Великий город Ленина», 
  изданный в Куйбышеве в самый страшный год для ленинградцев — 1942-й. В него 
  вошли стихи, рассказы и очерки Вс. Иванова, Веры Инбер и других писателей, посвященные 
  началу блокады и героизму горожан. Запрещен он, в отличие от других, по приказу 
  Куйбышевского обллита, поскольку в нем снова обнаружилось обращение «Ко всем 
  трудящимся города Ленина», подписанное П. Попковым. <br>
  <br>
  Сразу же после освобождения Лениздат выпустил в 1944 году сборник «Женщины города 
  Ленина. Рассказы и очерки о женщинах Ленинграда в дни блокады». В него, в числе 
  прочего, вошли стихи Вс. Рождественского, В. Инбер, Н. Тихонова и других поэтов. 
  Однако сборник открывается предисловием все того же П. Попкова, в котором он 
  благодарит женщин Ленинграда за совершенные ими трудовые подвиги. И снова — 
  лапидарная резолюция цензора: «На с. 12 подпись Попкова. На с. 7—9 — выступление 
  Попкова». (5)<br>
  <br>
  Цензурное ведомство пошло даже на такую крайнюю меру, как конфискация отдельных 
  номеров журнала «Звезда» (1947, № 3; 1948, № 10-11). Обычно она считалась экстраординарной, 
  ибо пробелы в годовых комплектах журнала вызывали «неуместные» вопросы и разговоры 
  среди читателей. В протоколе заседания бюро Ленинградского горкома ВКП(б) «Об 
  изъятии политически вредных книг и брошюр из библиотек Ленинграда» 17 февраля 
  1950 года тем не менее зафиксировано: «Изъять № 3 журнала "Звезда&quot; за 1947 
  г.: на с. 148-149 выдержки из выступления Кузнецова, &lt;а также&gt; сдвоенный 
  № 10-11 за 1948 год: на с. 164 в статье "Тема нашего искусства&quot; есть упоминание 
  о выступлении Кузнецова, посвященное восстановлению промышленности Ленинграда». 
  Не исключено, что эти номера подлежали изъятию и уничтожению и за пределами 
  Ленинграда. (6) Нужно, конечно, учесть и то обстоятельство, что после выхода 
  августовского постановления ЦК 1946 года «проштрафившаяся» «Звезда» находилась 
  под особым надзором партийных и цензурных органов. Не доверяя «ленинградским 
  товарищам», ЦК определил в главные редакторы «Звезды» А. М. Еголина, ортодоксального 
  литературоведа, занимавшего в то время пост заместителя начальника Управления 
  пропаганды и агитации ЦК (он вместе с Г. Ф. Александровым, своим начальником, 
  и готовил материалы к постановлению ЦК и докладу Жданова). Все верстки журнальных 
  номеров доставлялись ему в Москву, откуда он и руководил «Звездой». <br>
  <br>
  Тотальная кампания по искоренению неугодных имен коснулась и Музея обороны Ленинграда. 
  По воспоминаниям Г. И. Мишкевича, заместителя директора по научной работе музея 
  в конце 40-х годов, экскурсовода было дано указание: «У портретов Попкова, Кузнецова 
  не останавливаться». На картине «Жданов и Кузнецов у аппарата Бодо» фигура Кузнецова 
  была замазана: на ее месте нарисовали... окно». (7) Затем все «подозрительные» 
  картины и фотографии приказано было снять. Хорошо известна драматическая участь 
  этот музея, начавшегося с выставки «Великая Отечественная война советского народа 
  против немецких захватчиков», созданной по самым свежим следам событий весной 
  1942 года. Экспозиция открылась весной 1942 года на углу 1-й Красноармейской 
  улицы и Измайловского проспекта; затем, в конце 1943 года, была перенесена в 
  Соляной городок под название «Героическая оборона Ленинграда». Созданием выставки, 
  преобразованной затем в Музей обороны Ленинграда, руководил известный историк 
  и искусствовед, занимавший позднее посты директора Государственной Публичной 
  библиотеки, а затем Библиотеки Академии художеств Лев Львович Раков. В связи 
  с начавшимся все тем же «ленинградским делом» в августе 1949 года музей приказано 
  было закрыть для посетителей, Л. Л. Раков и четверо других сотрудников подверглись 
  политическим репрессиям за «прославление антипартийной группы» и «террористическую 
  деятельность». «Основанием» для последнего обвинения послужило хранение в музее 
  образцов оружия с остатками пороха. В 1952 году музей ликвидирован, многие образцы 
  военной техники сданы на переплавку, некоторые экспонаты уничтожены или переданы 
  в другие музеи. Музей обороны Ленинграда восстановлен только в 1989 году. (8)<br>
  <br>
  К нашему сюжету непосредственное отношение имеет звучавшее в годы разгона музея 
  обвинение сотрудников в том, что они занимались «пропагандой» экспонатов, в 
  основном фотографий, «натуралистически» изображавших ужасы блокадной зимы 1941-1942 
  года. <br>
  <br>
  Вот этот второй, «содержательный», мотив стал одним из главных во время «очистки» 
  библиотек от «идеологически чуждой» литературы. В списки Главлита попал, в частности, 
  солидный (512 с.) литературно-художественный сборник «Девятьсот дней. К 5-летию 
  освобождения Ленинграда от вражеской блокады» (Л.: Лениздат, 1948). В сборнике 
  обнаружен текст выступления все того же П. Попкова на митинге в 1942 году, но 
  главная его «вина» состояла в другом: «В сборнике, — фиксирует упоминавшийся 
  уже протокол заседания бюро Ленинградского горкома ВКП(б) "Об изъятии политически 
  вредных книг и брошюр из библиотек Ленинграда&quot; от 17 февраля 1950 года, 
  — опубликованы "Ленинградский дневник&quot; В. Инбер и "Февральский дневник&quot; 
  О. Берггольц. Авторы показывают жизнь города оторванной от общественной и политической 
  жизни страны. В подчеркнуто черном свете Инбер рисует ужасы блокады...» (9)<br>
  <br>
  По той же причине приказом Леноблгорлита № 5 от 14 ноября 1951 года подверглась 
  запрету повесть Ивана Федоровича Кратта (1899-1950) «Суровый берег», трижды 
  выпущенная в 1947 году различными ленинградскими издательствами. Повествует 
  она о людях героической ладожской трассы (Дороге жизни в годы войны), о страшных 
  бедствиях и настроениях людей в те годы. Мотивированного отзыва о ней в архивах 
  мне обнаружить не удалось, но, судя по всему, конфискована книга за «предоставление 
  трибуны врагу» (мотив, часто встречающийся в цензурных решениях) — на этот раз 
  «паникерам», сомневающимся в скорой победе и освобождении города. Хотя автор 
  и осуждает таких людей, приведенные им выдержки из дневника одного блокадника 
  не могли не обратить на себя внимания идеологических контролеров: «Ленинград 
  обречен, потому что у него нет надежды. Что будет с Россией?.. На что люди могут 
  рассчитывать?.. Продолжают рыть окопы. А немцы потом их используют...» Говоря 
  же о массовом бегстве из города, автор дневника задает уже явно «непатриотический» 
  и «безыдейный» вопрос, тут же отвечая на него: «Беженцы или эмигранты?.. Странники 
  по родной земле» (с. 29). <br>
  <br>
  Такую же участь, согласно приказу Леноблгорлита № 13 от 4 декабря 1951 года, 
  разделил сборник рассказов поэтессы и переводчицы Елизаветы Григорьевны Полонской 
  (1890-1969), входившей в 20-е годы в группу «Серапионовы братья». Под названием 
  «На своих плечах» сборник издавался дважды в 1948 году и, хотя «одиозные» имена 
  в нем отсутствуют, был все-таки запрещен, — скорее всего, за слишком мрачные 
  картины блокадной жизни Ленинграда, изображение страданий людей, голода и т. 
  п., о чем «лишний раз» напоминать не следовало. <br>
  <br>
  Весной 1942 года, побывав несколько раз в Ленинграде, начал работу над трилогией 
  о судьбах людей осажденного города А. Б. Чаковский, тогда — военный корреспондент 
  на Волховском и Ленинградском фронтах в 1941-1943 годах. Его повесть «Это было 
  в Ленинграде», вышедшая в 1945 году, впоследствии использованная для документально-художественного 
  повествования — романа «Блокада», вышедшего в пяти книгах (1968—1974), также 
  не избежала общей участи. Формальным мотивом изъятия ее послужила сцена встречи 
  автора с Попковым в Смольном. Рассказывая о положении в городе, Попков сказал, 
  в частности: «Сейчас главная задача — доставить в город продукты и организовать 
  борьбу с ворами и мародерами» (с. 96). Однако «сомнительными» могли показаться 
  сцены страданий ленинградцев и повальных смертей, публикация отрывков страшного 
  документа — дневника блокадницы. Писатель, посетив Ленинград, «видит какие-то 
  высохшие скелетообразные существа», умирающих от стужи и голода людей. Оказался 
  в спецхранах и 2-й номер «Ленинградского альманаха», вышедшего в 1948 году, 
  также, скорее всего, за публикацию слишком «откровенных и жестоких», с цензурной 
  точки зрения, сцен в «Записках военного хирурга» А. Коровина. <br>
  <br>
  В январе 1981 года в поле зрения цензоров снова попала «Звезда». По поводу готовящегося 
  3-го номера заместитель начальника Ленгорлита Г. К. Данилов направил в обком 
  информацию «для сведения». Претензии вызвали воспоминания Дмитрия Терентьевича 
  Хренкова (в то время он был главным редактором «Невы») «Осень в Переделкине», 
  посвященные в основном встречам и беседам с Н. С. Тихоновым. Вот что, в частности, 
  сообщалось:<br>
  <br>
  «Автор записок часто и широко цитирует слышанные им высказывания Н. С. Тихонова, 
  которые, как можно судить по тексту, носили в ряде случае доверительный характер, 
  и потому публикация их требовала осторожности, внимательности. А в некоторых 
  случаях соответствующего пояснительного комментария. Записки содержат положения, 
  которые односторонне рисуют моменты творческой биографии писателя. На стр. 164 
  т. Хренков приводит обширную выписку из его беседы с писателем, в которой говорилось 
  о желании Н. С. Тихонова написать книгу о блокаде Ленинграда. На вопрос т. Хренкова, 
  «почему не был "запущен в дело&quot; написанный вместе с Саяновым сценарий о 
  блокаде Ленинграда, Н. С. Тихонов якобы ответил: "Нельзя отделить подвиг от 
  страданий. А кое-кому кажется — можно... Подумать только, — в сердцах говорил 
  он, — нашлись авторы, которые с самым серьезным видом утверждают, что снабжение 
  Ленинграда в блокаде было организовано образцово и что число потерь от голода 
  явно преувеличено!&quot; Основываясь на этом, автор делает вывод, что книга 
  о блокаде Ленинграда не была написана Тихоновым... не только потому, что он 
  был перегружен текущими неотложными делами, но и потому, <em>что не знал, хватит 
  ли у него сил на полемику</em>&quot;. На стр. 163-164 приводятся высказывания 
  Н. С. Тихонова о А. А. Кузнецове». (10)<br>
  <br>
  Цензор высказывает еще ряд претензий к третьему номеру «Звезды», но воспоминания 
  Д. Т. Хренкова все же были в нем публикованы. Кое-что редакции удалось отстоять 
  — например, упоминание Тихоновым А. А. Кузнецова, без которого нельзя «представить 
  себе 900 дней битвы за Ленинград». Но не обошлось, как всегда, и без жертв, 
  Так, например, вместо выделенных курсивом слов появились другие: «...что просто 
  она еще не созрела в нем» (с. 164). Наиболее драматично, пожалуй, сложилась 
  судьба книги Ольги Федоровны Берггольц «Говорит Ленинград», выпущенная Лениздатом 
  в 1946 году тиражом 15 тыс. экземпляров. В книгу вошли стихи и ставшие уже легендарными 
  тексты почти ежедневных радиопередач Ольги Берггольц, обращенных к жителям осажденного 
  блокадного города: недаром ее называли тогда ленинградской мадонной. О ее подвижнической 
  деятельности в тот период написано уже немало (11), однако цензурная судьба 
  произведений поэтессы пока еще не исследована. Книга подверглась изъятию из 
  обращения согласно приказу самого начальника Главлита СССР (№ 1302 от 17 ноября 
  1949 года). (12)<br>
  <br>
  Показательно, что в дальнейшем она попадала практически во все запретительные 
  списки Главлита и освобождена из спецхранов одной из последних — только в 1991 
  году. Формальным поводом запрещения стало включения в сборник очерка «Севастополь», 
  написанного вскоре после освобождения этого города. Мотив: «На стр. 7, 31, 135, 
  138-139 положительно упоминается старший научный сотрудник Херсонесского историко-археологического 
  музея А. К. Тахтай, который, по данным соответствующих организаций, во время 
  Великой Отечественной войны сотрудничал и работал у немцев». (13) Обвинение 
  в этом Тахтая, крупного крымского археолога (14), сохранившего коллекции музея 
  в годы войны и оккупации, впоследствии было снято. Вот что пишет Ольга Берггольц: 
  «Какое пиршество для очей археолога, — сказал ученый хранитель Херсонесского 
  городища А. К. Тахтай, — какое грустное пиршество...»; «Мы ловили голос Ленинграда 
  с особым трепетом, — рассказывал Тахтай, — это был голос собрата по судьбе...» 
  <br>
  <br>
  На деле же конфискация сборника, как видно из других цензурных документов, объясняется 
  общей его тональностью, трагическими нотами выступлений Ольги Берггольц по радио 
  (а какими они должны были быть в то время?). Эта история неожиданно возникла 
  спустя 12 лет, когда готовился в издательстве «Советский писатель» сборник Берггольц 
  «Очерки и рассказы». При обсуждении рукописи на заседании редсовета, отнесшегося 
  к сборнику положительно, началась полемика по чисто «техническому» вопросу: 
  считать ли цикл «Говорит Ленинград» первым или вторым изданием. Приведу выдержки 
  из протокола заседания, обнаруженного в архиве: «<em>В. Н. Орлов</em>: Книга 
  "Говорит Ленинград&quot; выходила отдельной книгой в 1946 г. <em>В. К. Кетлинская</em>: 
  Книга выходила в Ленинграде и была изъята из-за Тахтафия (так!), севастопольского 
  хранителя Херсонеса. <em>А. Л. Слонимский</em>: Книга фактически не была дана 
  читателю. Как это может считаться переизданием? Это чисто формальный подход...» 
  (15)<br>
  <br>
  В упомянутом выше изъятом сборнике «900 дней», помимо «Ленинградского дневника» 
  В. Инбер, внимание цензоров привлек и «Февральский дневник» Ольги Берггольц. 
  Как видно из протокола заседания бюро Ленинградского горкома ВКП(б) от 17 февраля 
  1950 года, «в поэме Берггольц преобладает чувство обреченности, пессимизма, 
  содержатся элементы так называемой кладбищенсюй поэзии». (16) В официальных 
  партийных кругах с тех пор за ней прочно укрепилось имя «плакальщицы» — в уничижительно-пренебрежительном 
  оттенке этого слова. <br>
  <br>
  Оказался в спецхранах коллективный сборник «Стихи и поэмы. 1917—1947» (М.: Правда, 
  1947). Формальным поводом послужило включение стихотворения Н. Тихонова «Югославия», 
  содержащего строчки: «Поют они песни о Тито, / О Сталине песни поют...» Как 
  известно, в период разрыва отношений с «братской» Югославией в 1948-1953 годах 
  все хвалебные отзывы о Тито исключались: наоборот, он должен был представлен 
  как «кровавый палач» и «наймит англо-американского империализма». Однако включение 
  в сборник «Февральского дневника» усугубило его «вину». <br>
  <br>
  Несколько раз переходила из открытых фондов библиотек и обратно документальная 
  повесть В. Азарова и А. Зиначева «Живые, пойте о нас!» (издавалась дважды — 
  в 1969 и 1972 годах, каждый раз тиражом в 100 тыс. экземпляров). Приведу сейчас 
  пометки на карточках спецхранных каталогов Российской национальной библиотеки 
  и Библиотеки Российской академии наук: <em>«Запретить: Приказ Леноблгорлита 
  № 9. 11.11.1976. Вернуть в общие фонды: Приказ Ленгорлита № 12. 16.12.1977. 
  Приказ о возврате аннулировать (устное распоряжение Ленобгорлита (по телефону). 
  Действительным считать приказ № 9. 13.12.1978. Возвратить в общие фонды: Нет 
  в Сводном списке запрещенных книг 1988 г.». </em>(17) Повесть, в которую вкраплены 
  стихи Азарова, посвящена одной из военных операций, приведших к гибели множества 
  людей. Такими эпизодами, как известно, богата история Великой Отечественной 
  войны; чаще всего они тщательно скрывались. Авторы рассказывают о трагической 
  (и бессмысленной, хотя об этом говорится очень глухо) гибели заранее обреченного 
  десанта двух тысяч кронштадтских моряков, высаженных в Петергофе в октябре 1941 
  года. Весь отряд был тогда тотчас же окружен и почти полностью уничтожен. <br>
  <br>
  Последний цензурный эпизод, связанный с нашей темой, произошел уже в эпоху позднего 
  застоя — на рубеже 70—80-х годов. В 1975 году Даниил Гранин и Алесь Адамович 
  стали собирать материал для «Блокадной книги» — дневники, воспоминания, фотографии 
  и другие материалы ленинградцев, переживших ужас 1941—1943 годов. Сам Даниил 
  Гранин, оставивший свидетельства о цензурном прохождении этой книги (см. главы 
  «О блокаде» и «Запретная глава» в его книге «Тайный знак Петербурга». СПб., 
  2001), сообщает: «Были истории настолько страшные, что мы не решились использовать 
  их, и до сих пор они не опубликованы». Но даже в таком усеченном виде написанная 
  книга резко расходилась с принятым стереотипом в его идеологической неприкосновенности 
  и незыблемости. Согласно ему, блокадная тематика почти полностью должна ограничиваться, 
  чуть ли не исчерпываться «показом» героических и трудовых подвигов ленинградцев 
  в те годы. Разумеется, все это имело место, зафиксировано немало случаев самопожертвования, 
  необыкновенного мужества, проявленного жителями города. Но было и другое... 
  Как и во всякой экстремальной ситуации — на войне, в тюрьме, в осаде и т. п., 
  — в человеке проявляется то, что на него похоже, то, что в обычных условиях 
  до времени дремлет и может вообще никогда не «актуализироваться». Как известно, 
  бывали тогда и случаи мародерства, и предательства близких, и даже людоедства. 
  «Мы спускались в такие бездны человеческой психики, человеческих поступков, 
  которые было бы слишком жестоко повторить»,— говорит писатель. И хотя в книге 
  эти эксцессы были сглажены, она тем не менее в 1979 году была отклонена всеми 
  ленинградскими журналами. Издательство, которое собиралось выпустить книгу, 
  обратилось в идеологический отдел обкома КПСС, который решительно высказался 
  за ее запрещение. Таким образом, <em>в Ленинграде</em> тогда книга так и не 
  увидела света. Первые два отдельных издания удалось выпустить только в Москве 
  (1979 и 1982), хотя и там, несмотря на все старания редакции «Нового мира» (в 
  нем первоначально печаталась «Блокада»), рукопись проходила с огромными затруднениями. 
  Авторы получили свыше 60 замечаний Главлита, которые главным образом сводились 
  к требованию удалить из книги наиболее «жестокие» сцены, изъять все упоминания 
  о воровстве и мародерстве, бездарности и равнодушии партийных властей, обрекших 
  город на вымирание. Решительно не согласилась цензура с приведенными авторами 
  статистическими данными, в особенности с указанной ими цифрой: свыше миллиона 
  погибших; на самом верху была установлена «планка»: ни в коем случае цифра не 
  должна превышать 600 тысяч. Из первых изданий книги выброшена была полностью 
  глава, в которой речь шла об интервью, которое с большим трудом Гранину все-таки 
  удалось взять у А. Н. Косыгина (тогда председателя Совета Министров СССР). В 
  годы войны Косыгин, ленинградец по происхождению, не раз бывал в осажденном 
  городе и все-таки пытался кое-что сделать для спасения его жителей. По мнению 
  писателя, включению этой главы помешала только что вышедшая тогда «главная книга» 
  о войне, а именно — воспоминания Л. И. Брежнева «Малая земля». «Косыгинская 
  глава», в которой речь шла о несоизмеримо более масштабных и трагических событиях, 
  могла быть, с точки зрения идеологов, «противопоставлена» книге тогдашнего генсека 
  партии и даже могла «заслонить» ее в глазах читателей. В Ленинграде «Блокада» 
  стала печататься только с 1984 года, выдержав затем еще три издания, частично 
  дополненные и исправленные. <br>
  <br>
  Подведем некоторые итоги и попытаем ответить на поставленный выше вопрос: чем 
  же все-таки объясняется столь подозрительное отношение к блокадной теме в партийно-цензурных 
  кругах? В первую очередь, конечно, тотальной установкой на «оптимизм», которым 
  должна быть пронизана советская литература, на необходимость активной «формовки» 
  ею «человека будущего», не страдающего никакой рефлексией, человека с незамутненным 
  ничем не омраченным сознанием. Страдания, голод, повальная смерть ленинградцев 
  — это не вписывалось в предписанный сверху парадно-фанфарный героизм, которым 
  должна быть овеяна литература о войне, отсюда и преследование долгое время «лейтенантской» 
  поэзии и прозы, обвинявшейся в так называемой «окопной правде» — слишком мрачной 
  «одностороннем» изображении военных будней. И второй — не менее, как я думаю, 
  важный мотив — «старый спор столиц между собою», если слегка перефразировать 
  Пушкина (у него — «славян»). После переезда правительства в Москву в марте 1918 
  года власть ревниво и с опаской стала относиться к «колыбели революции». Началось 
  это с Кронштадтского мятежа 1921 года, поддержанного, кстати сказать, рабочими 
  Путиловского завода, рабочей оппозиции 20-х годов, и, что имеет непосредственное 
  отношение к нашему сюжету, непомерно раздутого «ленинградского дела». Не только 
  в сталинском представлении, но и в представлении его наследников, Ленинград 
  - город опасный, в котором всегда тлеет недовольство, подспудное стремление 
  восстановить свой столичный статус. <br>
  <br>
  И все же главная причина столь щепетильного, мягко говоря, отношения цензуры 
  к блокадной тематике заключалась в постоянном стремлении власти превратить все 
  население России в инфантильных, пожизненно несовершеннолетних подданных – идеальный 
  материал для построения «светлого будущего».<br>
