	<br>
	  <strong>Эксклюзивное интервью "АиФ" японского офицера Хиро Оноды </strong><br>
  <br>
  <span class="header"><strong>ВОЛК-ОДИНОЧКА<br>
  Он воевал в джунглях тридцать лет, не зная, что война давно закончена</strong><br></span>
  <br>
  <em>"Аргументы и факты". 7-13 февраля 2007. №6 (1371).</em><br>
  <br>
  <img src="onoda1.jpg" alt="Подпоручик Хиро Онода" width="273" height="289"><br>
  <br>
  <br>
  <strong>5 марта 1974 г. весь мир потрясла сенсационная новость: на филиппинском 
  островке Лубанг местной полиции наконец-то сдался 52-летний подпоручик японской 
  армии, скрывавшийся в лесу… с конца 44-го!</strong><br>
  <br>
  ...Именно тогда майор армейской разведки Танигучи оставил его на лесной базе 
  вместе с тремя солдатами - совершать диверсии против войск США. Всё это время 
  Хиро Онода, не веря в то, что Япония капитулировала, совершал нападения на военные 
  патрули. Через пять лет сдался первый солдат, двое других погибли в стычках 
  - в 1972 году Онода остался один. На самурая чудом наткнулся японский турист 
  Норио Судзуки, и подпоручик поведал ему свою историю. Из Японии срочно доставили 
  майора Танигучи, ставшего книготорговцем. Танигучи три дня ездил по джунглям 
  на джипе с громкоговорителем, вещая: "Я приказываю тебе сдаться!" Онода вышел 
  из леса, президент Филиппин помиловал японца. Дома его встречали как национального 
  героя. Сейчас 85-летний Хиро Онода живёт в Токио, избегая общения с прессой. 
  В августе 2004 года "АиФ" опубликовал статью "Последний самурай" о судьбе подпоручика, 
  которая вызвала небывалый читательский интерес и сотни писем с вопросами: каким 
  образом тому удалось прожить столько лет в экстремальных условиях? Ответить 
  на них мог только сам Онода. После трёх лет переговоров он согласился на краткую 
  встречу с обозревателем "АиФ".<br>
  <br>
  <img src="onoda2.jpg" alt="Статья &quot;Последний самурай&quot;, 2004" width="377" height="376"><br>
  <br>
  <br>
  <strong>"Болел только один раз"</strong><br>
  <br>
  <strong>- Честно говоря, я тоже не представляю, как можно 30 лет скрываться 
  в джунглях...</strong><br>
  <br>
  - Человек в мегаполисах слишком оторвался от природы. На самом деле в лесу есть 
  всё, чтобы выжить. Масса лекарственных растений, повышающих иммунитет, служащих 
  как антибиотик, обеззараживающих раны. Умереть с голоду тоже невозможно, главное 
  для здоровья - соблюдать нормальный режим питания. Например, от частого потребления 
  мяса температура тела повышается, а от питья КОКОСОВОГО молока -напротив, понижается. 
  За всё время в джунглях я болел только один раз. Не следует забывать об элементарных 
  вещах - утром и вечером я чистил зубы толчёной пальмовой корой. Когда меня потом 
  осматривал дантист, то он поразился: за 30 лет у меня не было ни единого случая 
  кариеса.<br>
  <br>
  <strong>- Что первым делом нужно научиться делать в лесу?</strong><br>
  <br>
  - Извлекать огонь. Сначала я поджигал стеклом порох из патронов, но боеприпасы 
  требовалось беречь, поэтому пробовал получить пламя при по мощи трения двух 
  кусков бамбука. Пусть не сразу, но в итоге у меня это получилось. Огонь нужен 
  для того, чтобы кипятить речную и дождевую воду, - это обязательно, в ней есть 
  вредные бациллы.<br>
  <br>
  <strong>- Когда вы сдались, то вместе с винтовкой отдали филиппинской полиции 
  500 патронов в отличном состоянии. Как у вас сохранилось столько боеприпасов?</strong><br>
  <br>
  - Я экономил. Патроны шли строго на перестрелки с военными, а также на то, чтобы 
  добыть свежее мясо. Изредка мы выходили на окраины деревень и ловили отбившуюся 
  от стада корову. Животное убивали одним выстрелом в голову и только во время 
  сильного ливня: так жители деревни не слышали звуков стрельбы. Говядину мы вялили 
  на солнце, делили её так, чтобы тушу коровы можно было съесть за 250 дней. Винтовку 
  с патронами я регулярно смазывал говяжьим жиром, разбирал, чистил. Вообще, берёг 
  её, как ребёнка, - закутывал в ветошь, когда было холодно, закрывал своим телом, 
  когда шёл дождь.<br>
  <br>
  <strong>- Чем вы ещё питались кроме вяленой говядины? </strong><br>
  <br>
  - Варили кашу из зелёных бананов в кокосовом молоке. Ловили рыбу в ручье, пару 
  раз совершили налёт на магазин в деревне, забрали рис и консервы. Ставили ловушки 
  на крыс. В принципе в любом тропическом лесу нет ничего опасного для человека.<br>
  <br>
  <strong>- А как насчёт ядовитых змей и насекомых?</strong><br>
  <br>
  - Когда вы годами находитесь в джунглях, то становитесь их частью. И понимаете, 
  что змея никогда просто так не нападёт - она сама вас до смерти боится. То же 
  и с пауками - они не ставят цель охотиться на людей. Достаточно не наступать 
  на них - тогда всё будет нормально. Разумеется, с самого начала лес будет очень 
  страшен. Но через месяц привыкнете ко всему. Мы опасались вовсе не хищников 
  или змей, а людей - даже суп из бананов варили исключительно ночью, чтобы дым 
  не увидели в деревне.<br>
  <br>
  <br>
  <strong>"Больше всего не хватало мыла"</strong><br>
  <br>
  <strong>- Вы не жалеете, что по тратили лучшие годы своей жизни на то, чтобы 
  вести бессмысленную партизанскую войну в одиночку, хотя Япония уже давно сдалась?</strong><br>
  <br>
  - В императорской армии не принято обсуждать приказы. Майор сказал: "Ты должен 
  оставаться, пока я не вернусь за тобой. Это приказание могу отменить только 
  я". Я солдат и выполнял приказ - что тут удивительного? Меня оскорбляют предположения, 
  что моя борьба была бессмысленной. Я воевал, чтобы моя страна стала могущественной 
  и процветающей. Когда я вернулся в Токио, то увидел, что Япония сильна и богата 
  - даже богаче, чем прежде. И это утешило моё сердце. Что касается остального... 
  Откуда же я мог знать, что Япония капитулировала? Я и в страшном сне не мог 
  себе это представить. Всё это время, что мы сражались в лесу, были уверены - 
  война продолжается.<br>
  <br>
  <strong>- Вам сбрасывали с самолёта газеты, чтобы вы узнали о капитуляции Японии.</strong><br>
  <br>
  - Современное типографское оборудование может напечатать всё, что нужно спецслужбам. 
  Я решил, что эти газеты фальшивые - их изготовили враги специально для того, 
  чтобы обмануть меня и выманить из джунглей. Последние два года с неба бросали 
  письма моих родственников из Японии, которые уговаривали меня сдаться, - я узнал 
  почерк, но думал, что американцы их взяли в плен и заставили написать такие 
  вещи.<br>
  <br>
  <strong>- В течение тридцати лет вы воевали в джунглях с целой армией - против 
  вас в разное время задействовали батальон солдат, отряды спецназа, вертолёты. 
  Прямо скажем - сюжет голливудского кинобоевика. У вас нет ощущения, что вы супермен?</strong><br>
  <br>
  - Нет. С партизанами всегда тяжело воевать - во многих странах десятилетиями 
  не могут подавить вооружённое сопротивление, особенно в труднопроходимой местности. 
  Если ты чувствуешь себя в лесу как рыба в воде - твой противник попросту обречён. 
  Я чётко знал - по одной открытой местности следует перемещаться в камуфляже 
  из сухих листьев, по другой - только из свежих. Филиппинские солдаты не были 
  в курсе таких тонкостей.<br>
  <br>
  <strong>- Чего вам больше всего не хватало из бытовых удобств?</strong><br>
  <br>
  - Трудно сказать. Мыла, наверное. Я стирал одежду в проточной воде, используя 
  золу от костра как чистящее средство, и умывался каждый день... но очень хотелось 
  намылиться. Проблема была в том, что форма начала расползаться. Я изготовил 
  иголку из обломка колючей проволоки и штопал одежду нитками, которые сделал 
  из побегов пальмы. В сезон дождей жил в пещере, в сухой сезон строил себе &quot;квартиру&quot; 
  из бамбуковых стволов и покрывал крышу пальмовой &quot;соломкой&quot;: в одной 
  комнате была кухня, в другой спальня. <br>
  <br>
  <strong>- Как вы пережили возвращение в Японию? </strong><br>
  <br>
  - С трудом. Как будто я из одного времени сразу перенёсся в другое: небоскрёбы, 
  девушки, неоновая реклама, непонятная музыка. Я понял, что у меня произойдёт 
  нервный срыв, всё было чересчур доступно - питьевая вода текла из крана, еда 
  продавалась в магазинах. Я не мог спать на кровати, всё время ложился на голый 
  пол. По совету психотерапевта я эмигрировал в Бразилию, где разводил коров на 
  ферме. Только после этого я смог вернуться домой. Но и сейчас на три месяца 
  в году я уезжаю в горные районы Хоккайдо: там я основал школу для мальчиков, 
  где учу их искусству выживания. <br>
  <br>
  <strong>- Как вы предполагаете: может ли кто-то из японских солдат и сейчас 
  скрываться в глубине джунглей, не зная, что кончилась Вторая мировая война? 
  </strong><br>
  <br>
  - Возможно, ведь мой случай не был последним. В апреле 1980 года сдался капитан 
  Фумио Накахира, который 36 лет прятался в горах филиппинского острова Миндоро. 
  Не исключено, что в лесах остался кто-то ещё... <br>
  <br>
  <strong>- Но если бы майор Танигучи не отменил свой приказ... вы воевали бы 
  до сих пор? </strong><br>
  <br>
  - Да.<br>
