  Юмористическая фронтовая песенка на мотив популярного фокстрота Шолома Секунды 
  &quot;Bei Mir Bistu Shein&quot; (&quot;Для меня ты самая красивая...&quot;, 
  слова на идиш Якоба Якобса, 1932, США) - в России эта мелодия более всего известна 
  по песне <a href="../../dvor/vkejptaunskom.php">&quot;В Кейптаунском порту&quot;</a> 
  (там же см. ноты).<br>
  <br>
  Написана в 1942-43 годы для  джаз-оркестра театра 
  балтийского флота п/у Николая Минха, и этим же оркестром в те же годы записана на пластинку в блокадном Ленинграде. В первоначальном варианте пелось про Ленинград: 
  <em>&quot;Орал по радио, что в <strong>Ленинграде</strong> он...&quot;</em>). 
  Песню подхватил Утесов и пел как про Ленинград, так и  про Сталинград.<br>
  <br>
  Ноты для фортепиано - см. <a href="../../popular-in/staruchka.htm">здесь.</a><br>
  <br>
  Подробнее о судьбе &quot;Bei Mir Bistu Shein&quot; см. Давид Эйдельман <a href="../../dvor/izr/a-beimir.php" tar>&quot;Bei 
  mir bist du scheyn&quot; или &quot;В кейптаунском порту&quot;</a> (2007) и Алика 
  Калайда <a href="../../dvor/izr/a-typomkejp.php">&quot;Ты помнишь Кейптаунский 
