  <em>Русские советские песни (1917-1977). Сост. Н. Крюков и Я. Шведов. 
  М., «Худож. лит.», 1977, дата: 1942.</em><br>
<br>
  <br>
  Речь идет о зиме 1941-42 годов. В некоторых сборниках песня датируется 1942 годом, 
  но в основном - 1941-м. В таком случае, это декабрь 1941-го. Одесса, отразив 
  несколько штурмов наступающей 4-й румынской армии, была оставлена 16 октября 
  1941 года. Ее гарнизон был эвакуирован морем в Севастополь, где продолжил борьбу. 
  Освобождена Одесса 10 апреля 1944 года. Текст в разных источниках варьируется. 
  На фонограммах Клавдии Шульженко в третьем куплете - <em>&quot;А когда не будет 
  фашистов и в помине&quot;</em>, в другом источнике - <em>&quot;А когда не станет 
  горя и в помине&quot;.</em><br>
  <br>
  <br>
  Юрий Москаленко <a href="../../drugije/a-otkudarod.php">&quot;Откуда родом автор 
  песни «Ах, Одесса, жемчужина у моря»?&quot;</a> (2008): <br>
  <br>
  <em>&quot;...вначале родилась мелодия, а уж потом к ней были написаны стихи. 
  Дело в том, что с началом войны Модеста Ефимовича назначили музыкальным руководителем 
  Ансамбля песни и пляски 2-й гвардейской армии. В программу к 24-й годовщине 
  Октябрьской революции, осенью 1941 года, композитор сочинил несколько мелодий, 
  которые предложил своему соавтору Илье Френкелю. Одна из этих мелодий стала 
  песней «Давай закурим».<br>
  <br>
  Вот только со словами случилась конфуз. Бригадный комиссар Рюмин, возглавлявший 
  отдел пропаганды и агитации Южного фронта, которому композитор принес и пропел 
  эту песню, тоном, не терпящим возражений, заявил: «Никому эта твоя песня не 
  нужна. Что это я буду вспоминать про то, что ты дал мне закурить? Вот если бы 
  снарядами поделился или автоматный диск с патронами передал бы, тогда другое 
  дело». Не очень-то верил в успех и первый исполнитель песни – Аркадий Явник 
  (Воронцов). Но песня упорхнула и тут же стала любимой у фронтовиков. Про песню 
  «Давай закурим» Константин Симонов как-то сказал, что не было такого фронта, 
  на котором бы ее не пели. И что написать эту песню мог только человек, по-настоящему 
  знающий, что такое война и что такое солдатская жизнь на войне…</em>&quot;<br>
