Кивают листвой тополя…<br>
Каховка, Каховка! Ты вновь перед нами –<br>
Родная, святая земля!<br>
Мы шли через горы, леса и долины,<br>
Прошли через гром батарей,<br>
Сквозь смерть мы пробились… Встречай, Украина,<br>
Своих дорогих сыновей!<br>
