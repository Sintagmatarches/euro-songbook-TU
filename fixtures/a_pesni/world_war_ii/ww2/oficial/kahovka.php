Греми, наша ярость! Вперед, бронепоезд,<br>
На Запад, на Запад лети!<br>
<br>
Пожары легли над Каховкой родимой,<br>
Кровава осенняя мгла,<br>
И песни не слышно, и в сердце любимой<br>
Немецкая пуля вошла.<br>
За юность, на землю упавшую рядом,<br>
За Родины славу и честь<br>
Забудем, товарищи, слово «Пощада»,<br>
Запомним, товарищи, «Месть!»<br>
