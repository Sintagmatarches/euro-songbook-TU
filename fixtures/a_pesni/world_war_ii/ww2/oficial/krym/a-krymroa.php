  • Два отделения (восточной) «охотничьей команды» (2x Ost-Jagdkommando)<br>
  <br>
  Это составляло от 2000 до 4000 человек, причем почти 1/3 из них в конце 1943 
  — начале 1944 г. перешла на сторону партизан. Это привело к тому, что немцы 
  отвели с передовой некоторые части РОА, разоружили их, а личный состав заключили 
  в концлагеря. По донесениям советских разведчиков, такой случай имел место в 
  марте 1944 г. в Евпатории. А с 3 по 12 декабря 1943 г. около батальона было 
  переброшено из Крыма в Италию и во Францию [44].<br>
  <br>
  Конец деятельности частей РОА в Крыму был трагическим, но закономерным, когда 
  в ходе наступления Красной Армии в апреле-мае 1944 г. была разгромлена немецкая 
  группировка на полуострове. По мнению современного российского историка К.М. 
  Александрова, «все русские добровольческие формирования вермахта, дислоцированные 
  в Крыму, погибли в ходе (этих. — О.Р.) операций». Еще более трагическая участь 
  постигла В.И. Мальцева. В 1946 г. он был передан западными союзниками в руки 
  советских органов и в ночь на 1 августа 1946 г. после закрытого судебного процесса 
  был повешен во дворе Бутырской тюрьмы в Москве вместе с Власовым и другими высшими 
  офицерами РОА [45].<br>
  <br>
  Исходя из всего сказанного, можно сделать вывод, что в военном отношении власовское 
  движение и РОА не представляли на территории Крыма сколько-нибудь значительной 
  силы. Приведенные выше цифры общего количества личного состава частей РОА свидетельствуют 
  о том, что к весне 1944 г. оно составляло всего лишь 0,35% от численности русского 
  населения полуострова (555481 чел. по состоянию на 1939 г.). Кроме того, надо 
  учесть и тот факт, что около 3/4 частей РОА на территории полуострова были укомплектованы 
  добровольцами не из Крыма.<br>
  <br>
  Однако следует признать, что, несмотря на слабость в военном отношении, сила 
  этих формирований заключалась в отношении политическом. Указанные факты свидетельствуют 
  о том, что это признавало даже советское военно-политическое руководство, хотя 
  и делало вид, будто «проблемы РОА» не было и нет. Такая позиция была характерна 
  не только для крымской ситуации. Однако здесь политико-пропагандистская роль 
  РОА проявилась наиболее сильно, что и заставило советское руководство провести 
  целый комплекс соответствующих мероприятий.<br>
