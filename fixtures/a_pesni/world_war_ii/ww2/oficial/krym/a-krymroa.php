	<strong>Олег Романько, <br>
	кандидат исторических наук, доцент кафедры украиноведения Крымского 
  государственного медицинского университета им. С.И. Георгиевского (Симферополь)</strong><br>
  <br>
<span class="header"><strong>ФОРМИРОВАНИЯ РУССКОЙ ОСВОБОДИТЕЛЬНОЙ АРМИИ НА ТЕРРИТОРИИ 
  КРЫМА (1943–1944) </strong><br></span>
  <br>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=87&par=74&l=&art=326" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, №8, 
  2004.</em></a><br>
  <br>
  <br>
  Одной из самых трагических и вместе с тем малоизученных страниц Второй мировой 
  войны является история власовского движения и созданных в результате его военно-политического 
  развития вооруженных сил, известных как Русская освободительная армия (РОА).<br>
  <br>
  Что это была за армия? Кто шел служить в эту армию? Наконец, что побуждало людей 
  добровольно вступать в нее? Долгое время на эти и многие другие вопросы мы не 
  могли получить ответа. Благодаря демократическим переменам, происшедшим в нашем 
  обществе за последнее десятилетие, эти вопросы были отчасти разрешены. Тем не 
  менее некоторые из них до сих пор остаются без внимания историков. Одним из 
  таких самых, на наш взгляд, неисследованных и запутанных, остается вопрос об 
  истории создания и деятельности частей РОА на территории оккупированного Крыма. 
  Ответить на него и является задачей данного исследования.<br>
  <br>
  С осени 1942 г. важным фактором, впоследствии повлиявшим на изменения в немецкой 
  национальной и оккупационной политике, стало власовское движение. Это движение 
  получило свое название и было неразрывно связано с именем бывшего генерал-лейтенанта 
  Красной Армии А.А. Власова [1], который в июле 1942 г. попал в плен к немцам 
  на Волховском фронте.<br>
  <br>
  Находясь в лагере военнопленных, Власов изъявил желание сотрудничать с некоторыми 
  представителями германского командования, которые в противовес убежденным нацистам 
  выступали за союз с некоммунистической Россией, а также готовили убийство Гитлера 
  и государственный переворот с концептуальным изменением целей войны. В обмен 
  за сотрудничество он просил поддержки в деле «освобождения России от большевиков». 
  Власов намеревался создать новое правительство новой независимой России, «свободной 
  от большевиков и капиталистов», а его вооруженными силами должна была стать 
  антисталинская армия — РОА.<br>
  <br>
  Однако немцы решили использовать его прежде всего как инструмент своей пропаганды 
  в войне с СССР. И одним из первых их шагов в этом направлении была разработка 
  листовки — «Обращение Русского комитета к бойцам и командирам Красной Армии, 
  ко всему Русскому народу и другим народам Советского Союза». Она была напечатана 
  в Берлине 27 декабря 1942 г. и подписана Власовым (председатель Русского комитета) 
  и другим пленным советским генералом — В.Ф. Малышкиным [2] (секретарь комитета). 
  В этом «Обращении», получившем название «Смоленского манифеста» (считалось, 
  что именно там, ближе к России и к тем, кому адресовалось, оно и было написано), 
  провозглашалось создание нового правительства — Русского комитета (который так 
  и остался на бумаге) и его вооруженных сил — РОА. Главной целью их существования 
  являлось «свержение Сталина и его клики, уничтожение большевизма» [3].<br>
  <br>
  Листовка имела некоторый успех и прежде всего на оккупированных территориях, 
  поэтому германское командование приняло решение более активно использовать Власова 
  и его сторонников в целях пропаганды, положив начало так называемой акции Власова. 
  Одним из ее результатов была организация в феврале 1943 г. координационного 
  центра, который должен был, как писал один из участников этих событий офицер 
  абвера В.К. Штрик-Штрикфельдт, «изучать политические и психологические проблемы 
  Русского освободительного движения» [4] — так стали называть всех сторонников 
  Власова. К марту 1943 г. центр разросся в крупную школу по подготовке пропагандистов 
  для «восточных» добровольческих формирований вермахта (набранных из граждан 
  СССР), которая по своему расположению в местечке недалеко от Берлина получила 
  название «Дабендорф» [5].<br>
  <br>
  Здесь следует сделать отступление и выяснить, что же все-таки представляла собой 
  РОА, разобраться с этим, так вроде бы хорошо известным и вместе с тем неизвестным 
  термином. Еще А.И. Солженицын в свое время писал, что искать определение этого 
  термина «было для лиц неофициальных — опасно, для официальных — нежелательно» 
  [6]. Поэтому до сих пор даже в серьезных исторических исследованиях, посвященных 
  Второй мировой войне, бытует мнение, что «РОА — власовская армия», возникла 
  она сразу после провозглашения ее в «Смоленском манифесте» и просуществовала 
  без изменений до конца войны.<br>
  <br>
  Все это не так. Как уже можно понять из истории появления манифеста (да и из 
  целей, с которыми он был написан), РОА связывалась с именем Власова только в 
  пропагандистском отношении. Но даже и РОА как оперативного формирования, подчиненного 
  Власову, никогда не существовало, ее… вообще не существовало. Не началась ее 
  организация и после опубликования «Смоленского манифеста». Только позднее, весной 
  1943 г., почти все «восточные добровольческие формирования» вермахта (обычно 
  численностью не более батальона), укомплектованные русскими добровольцами, стали 
  называться «батальоны РОА». Ни Власов, ни его окружение не имели никакого отношения 
  к созданию, формированию, боевому использованию и командованию этими батальонами 
  уже по той простой причине, что они начали создаваться в августе-сентябре 1941 
  г., когда Власов еще защищал Киев! [7].<br>
  <br>
  В подтверждение этих слов ныне покойный известный немецкий историк И. Хоффманн 
  писал автору этого исследования, что «все солдаты русской национальности могли 
  считать себя с 1943 г. бойцами РОА — в данном случае… речь, в первую очередь, 
  шла только о пропаганде» [8].<br>
  <br>
  Все вышесказанное касается и «батальонов РОА» в Крыму, с той лишь разницей, 
  что они начали создаваться уже после «признания» Власова председателем Русского 
  комитета и «командующим РОА». Столь поздний срок можно объяснить лишь тем, что 
  власовская программа — «Смоленский манифест» — получила сравнительную известность 
  только в средней полосе России и практически осталась неизвестной на юге, тем 
  более в Крыму. Широкую известность на территории полуострова имя генерала получило 
  в связи со следующим по времени пропагандистским шагом немцев — появлением открытого 
  письма Власова «Почему я встал на путь борьбы с большевизмом?» (18 марта 1943 
  г.) [9]. Именно этому открытому письму, в котором он рассказывал «о своей жизни 
  и своем опыте в СССР», объяснял причины, «побудившие его начать войну против 
  сталинского режима», Власов был обязан своей популярностью в некоторых слоях 
  населения [10].<br>
  <br>
  В Крыму первым на это письмо публично отреагировал бургомистр Ялты В.И. Мальцев 
  [11]. Это был человек трагической и вместе с тем интересной судьбы. Бывший полковник 
  советских Военно-воздушных сил, необоснованно репрессированный в 1938 г., но 
  выпущенный за недостатком улик, он встретил войну на должности директора санатория 
  Аэрофлота в Ялте. «По занятии германскими войсками города, — писал он в своих 
  воспоминаниях «Конвейер ГПУ», — я в полной военной форме явился к германскому 
  командованию и объяснил причины, побудившие меня остаться» [12]. Немецкие оккупационные 
  власти оказали ему доверие, и до лета 1943 г. он занимал должности бургомистра 
  и мирового судьи Ялты, одновременно выступая в местной печати (в частности, 
  в газете «Евпаторийские известия»), в которой призывал население Крыма сотрудничать 
  с новой властью [13].<br>
  <br>
  Прочитав открытое письмо Власова, Мальцев решил присоединиться к нему и в марте 
  1943 г. подал рапорт о своем переводе в распоряжение Русского комитета. Позднее, 
  4 июня 1943 г., в органе Симферопольского городского управления газете «Голос 
  Крыма» был опубликован его ответ на письмо Власова. Ответ был написан также 
  в форме открытого письма и озаглавлен «Борьба с большевизмом — наш долг». В 
  этом письме Мальцев рассказывал, как он прошел путь от «коммунизма к борьбе 
  с ним», и призывал всех коммунистов последовать его примеру, отдав все силы 
  на благо русского народа, то есть поддержать Власова и РОА [14].<br>
  <br>
  Не прошло и месяца после опубликования этого письма Мальцева, как уже 18 июня 
  «Голос Крыма» сообщил об открытии при его содействии первого вербовочного пункта 
  РОА в Крыму. Этот пункт был открыт в Евпатории [15].<br>
  <br>
  30 июня 1943 г. в Симферополе состоялось освещение помещения центрального вербовочного 
  пункта РОА в Крыму. По этому случаю в 18 часов был отслужен торжественный молебен. 
  Пункт начал функционировать в здании на улице Таврической, 15/18 [16]. Как писала 
  газета «Голос Крыма», «он был открыт для проведения систематической разъяснительной 
  работы, консультации, записи добровольцев и оформления их в ряды РОА, так как 
  сотни лучших людей нашей Родины уже подали заявления о вступлении в ее ряды» 
  [17].<br>
  <br>
  Подчеркнем, что газета «Голос Крыма» проявляла большой интерес к теме РОА и 
  немало способствовала ее популяризации среди всех слоев населения Крыма. Так, 
  с этого же времени на ее третьей странице появилась рубрика «Уголок добровольца», 
  в которой помещалась информация, посвященная формированию РОА в Крыму, записи 
  добровольцев, их боевым действиям. Публиковалась также информация не только 
  крымского, но и общероссийского характера.<br>
  <br>
  Например, 29 марта 1943 г. была опубликована статья «Русская Освободительная 
  Армия», которая должна была доказать, что «служение народу и Родине в рядах 
  РОА есть высший долг каждого боеспособного гражданина России и его почетная 
  обязанность. Чистота знамени и идеи, нерушимость присяги Родине — таков лозунг 
  РОА».<br>
  <br>
  В номере от 23 мая 1943 г. раскрывались «Задачи Русского Освободительного Движения»: 
  «Русский народ обязан прежде всего напрячь свои силы к уничтожению советской 
  власти. Он должен сделать это во имя счастья грядущих поколений, во имя своего 
  будущего. Это сейчас главная цель, и в этом пока единственный прямой путь, сворачивать 
  с которого преступно».<br>
  <br>
  В статье «РОА и народы России» от 9 июля 1943 г. определялось ее место среди 
  уже существующих национальных добровольческих формирований: «РОА — это цемент, 
  добровольно связывающий все национальные вооруженные силы Новой России. РОА 
  — это основной союзник Германской Армии, ведущей всю тяжесть борьбы с большевизмом» 
  [18].<br>
  <br>
  В связи с этим будет небезынтересно упомянуть тот факт, что в крымских частях 
  РОА служили не только русские, но и крымские татары. Это подтверждает целый 
  ряд публикаций в «Голосе Крыма», свидетельствующих о «совместной борьбе татар 
  и русских против большевизма»: «Плечом к плечу с РОА», «Борьба татар против 
  большевизма», «Голос крови зовет меня» и т.п. Основной смысл этих статей можно 
  выразить следующей фразой из одной такой статьи: «Победа или смерть! Плечом 
  к плечу с Русской Армией мы пойдем на борьбу за наше освобождение» [19].<br>
  <br>
  Естественно, что все местное руководство РОА было тесно связано с органами немецкой 
  пропаганды, а именно со Штабом пропаганды «Крым» — главным инструментом психологической 
  войны на полуострове. Его начальник обер-лейтенант Фрей, в частности, рекомендовал 
  своим подчиненным использовать офицеров РОА «для непосредственного разъяснения 
  обстановки и использования в качестве докладчиков на радио и в печати» [20].<br>
  <br>
  Так, 11 июня 1943 г. капитан Б. Ширяев в «Доме воспитания» в Алуште читал собравшимся 
  там учителям лекцию на тему: «Немецкая система воспитания как основа высокого 
  жизненного уровня в государстве». Другие офицеры-пропагандисты, такие как капитаны 
  Л. Станиславский, Г. Барятинский, А. Таманский и другие, активно выступали со 
  статьями в местной печати, а поручик К. Быкович даже стал впоследствии главным 
  редактором газеты «Голос Крыма» [21].<br>
  <br>
  Пропагандисты РОА также привлекались для подготовки листовок и воззваний, обращенных 
  к бойцам Красной Армии и крымским партизанам, в которых последние призывались 
  переходить на сторону РОА и немцев. При их содействии были составлены листовки 
  со следующими характерными названиями: «Братья красноармейцы!», «К офицерам 
  и солдатам Красной Армии», «Товарищ, один вопрос..!» и т.п. [22].<br>
  <br>
  В конце июля 1943 г. в «восточных» добровольческих формированиях, лагерях военнопленных 
  и немецких дивизиях, расположенных в Крыму и имеющих русских добровольцев, появились 
  выпускники школы в Дабендорфе — офицеры-инспектора РОА, призванные «следить 
  за физическим и моральным состоянием своих соотечественников» [23]. Это было 
  еще одной из обязанностей офицеров РОА, и на территории Крыма в том числе.<br>
  <br>
  Как известно, весь 1943 г. прошел под знаком ухудшения положения германских 
  вооруженных сил на Восточном фронте. Это, естественно, не могло не сказаться 
  на ситуации с РОА, в том числе и в Крыму. Например, уже в январе 1944 г. в одном 
  из отчетов Штаба пропаганды «Крым», озаглавленном «Об откликах населения на 
  немецкую пропаганду», было отмечено некоторое ослабление энтузиазма крымчан 
  и местных властей по поводу РОА. В газете «Голос Крыма», говорилось в этом отчете, 
  «…совершенно не пишется о РОА. Нужно было что-нибудь писать о ней, даже если 
  она используется где-то на Итальянском фронте» [24].<br>
  <br>
  Одновременно с этим немецкие власти все-таки не рекомендовали распространять 
  власовские газеты, такие как «Доброволец», «Заря», «Боевой путь» и другие, среди 
  рабочих трудовых лагерей, опасаясь возникновения «русских фантазий» [25]. Скорее 
  всего, имелись в виду «фантазии» о русской национальной идее и так называемой 
  третьей силе, то есть использовании РОА в качестве инструмента в борьбе как 
  против большевиков, так и против нацистов для достижения своих целей. Так, в 
  том же отчете Штаба пропаганды указывалось, что «среди населения имеется много 
  сторонников… «третьей силы». Это — люди, ожидающие окончательного завершения 
  войны, которое наступит после полного поражения Германии и Советского Союза… 
  Совершенно определенно эти идеи косвенно или прямо направлены против немецких 
  интересов. Несмотря на это, «Голос Крыма» опубликовал уже несколько статей, 
  посвященных данному вопросу и созвучных общему мнению населения. Последней из 
  них является статья «Третья мировая война» в номере от 7 января 1944 г., где 
  речь идет о том, что Англия и Америка третью мировую войну будут вести против 
  СССР… А Германия… вычеркивается» [26]. При этом немцев беспокоило прежде всего 
  то, что такое убеждение «снизило страх перед возвращением большевиков», что 
  оно могло сказаться на лояльности населения к оккупационным властям и желании 
  совместно с ними защищать Крым.<br>
  <br>
  Это привело к тому, что теперь за пропагандистами-лекторами РОА было установлено 
  постоянное наблюдение, а для проведения лекций выработаны унифицированные образцы 
  докладов, которые утверждались в Штабе пропаганды «Крым» [27].<br>
  <br>
  Помимо чисто пропагандистских целей, части РОА в Крыму использовались и по своему 
  прямому назначению — для участия в боевых действиях против Красной Армии и партизан, 
  что было также своего рода пропагандой. Недаром, находясь еще в лагере военнопленных, 
  генерал Власов говорил немецким офицерам: «Чтобы добиться победы над Советским 
  Союзом, нужно ввести в бой против Красной Армии военнопленных. Ничто не подействует 
  на красноармейцев так сильно, как выступление русских соединений на стороне 
  немецких войск…» [28].<br>
  <br>
  После открытия вербовочных пунктов РОА началось формирование ее частей в Крыму. 
  Но об их общей численности судить очень трудно, так как немецкая сторона эту 
  численность явно завышала, заявляя о «тысячах добровольцев», а советская — преднамеренно 
  занижала, сообщая о том, что в «Крыму с треском провалилась кампания, проводимая 
  немцами по вербовке в РОА. Никакие угрозы, никакие подручные Власова, выступавшие 
  с воззваниями, не подействовали на советских людей — они в РОА не пошли… Своей 
  освободительной армией наши люди называют Красную Армию» [29].<br>
  <br>
  Тем не менее советское военно-политическое руководство считало, что РОА, даже 
  несмотря на свою малочисленность, может быть и опасной. Для того чтобы сорвать 
  набор добровольцев и очернить в глазах населения саму идею РОА и власовского 
  движения, был предпринят целый комплекс мероприятий военно-политического характера. 
  За линией фронта этим занимались органы советской пропаганды, в Крыму — партизаны 
  и подпольщики.<br>
  <br>
  Так, в ответ на начало вербовочной кампании РОА в Крыму Главное политуправление 
  Красной Армии выпустило в количестве 300 тыс. экземпляров листовку «Солдаты 
  и офицеры РОА». В ней была предпринята попытка повлиять на записывающихся в 
  эту армию добровольцев — и уже не только уговорами. «Вы стараетесь прикрыть 
  свою измену, — говорилось в листовке, — громкими фразами о «борьбе с большевизмом» 
  «за счастье народа», за «Новую Россию». Все это ложь — вы изменники. Вы просто 
  служите немцам, потому что вам там хорошо живется. Вы продались за сытную еду, 
  за красивую форму, за легкую жизнь. Немцы перед вами всячески заискивают, стараются 
  вам угодить, лишь бы вы дрались вместе с ними. Но вы просчитались…» [30].<br>
  <br>
  Всего же за период с 15 мая по 1 октября 1943 г. редакция газеты «Красный Крым» 
  (г. Краснодар) — основной орган печатной пропаганды на полуострове — выпустила 
  15 наименований листовок на тему о РОА (всего 375 тыс. экземпляров, по 25 тыс. 
  каждого наименования). Среди них такие, как: «К добровольцам», «Не идите в так 
  называемую «русскую освободительную армию»!», «К добровольцам и всем, кто обманут 
  Гитлером!», «К «добровольцам», полицейским, старостам и всем, кто работает на 
  немцев», «К солдатам и офицерам РОА» и т.п. [31].<br>
  <br>
  Если советская власть была еще вне пределов Крыма и могла действовать пока только 
  пропагандой, то партизанам и подпольщикам были даны указания срывать вербовку 
  в РОА любой ценой, вплоть до применения террора. В целом, своей пропагандой 
  и агитацией партизаны должны были создать атмосферу недоверия и нетерпимости 
  по отношению к РОА и тем, кто в нее записался. Для этого было необходимо ответить 
  на ряд вопросов, которые могли бы возникнуть у населения: «Что означает эта 
  новая авантюра Гитлера… Для чего нужна Гитлеру РОА?». Следовало отвечать так: 
  «Германии до зарезу нужны солдаты, нужно мясо для пушек. Для этого и нужна немцам 
  так называемая РОА». Гитлер хочет заставить русских людей воевать против России, 
  против своих единокровных братьев, за чуждые нам интересы немецких князей и 
  баронов». На очень существенный вопрос «из кого состоит РОА?» предполагалось 
  отвечать, что «она состоит из белогвардейцев, разгромленных в свое время Красной 
  Армией и бежавших за границу. Кроме того, немцы силой загоняют в эту армию военнопленных. 
  Гитлеровцы в своих газетах открыто пишут, что им нужна «белая армия»… Вот о 
  какой армии мечтают немцы, в какую армию они хотят загнать русских людей и заставить 
  воевать против своей отчизны… «РОА» — это наглый обман, это ловушка для русского 
  народа» [32].<br>
  <br>
  То есть, несмотря на принижение советской пропагандой значения РОА, работе по 
  срыву вербовки в нее отводилась «исключительно важная роль», — признает советский 
  историк В.П. Яковлев [33]. Вот только некоторые из акций крымских партизан и 
  подпольщиков, направленных против РОА. Так, по воспоминаниям феодосийского подпольщика 
  А.А. Овчинникова, вопрос о «срыве вербовки в РОА» был очень важным и не снимался 
  с повестки дня заседаний его подпольной группы до весны 1944 г. [34].<br>
  <br>
  Советской пропагандой среди добровольцев РОА занималась работавшая в период 
  войны в Симферопольской центральной библиотеке Е.К. Пахомова [35].<br>
  <br>
  Партизан из Евпатории Н. Каташук вспоминал: «Перед группой связанных со мной 
  товарищей я поставил задачу не допустить ни одного рабочего в так называемую 
  власовскую армию» [36].<br>
  <br>
  Диверсионная группа Симферопольской подпольной организации 26 февраля 1944 г. 
  пустила под откос воинский эшелон на перегоне Альма — Симферополь. Помимо уничтоженной 
  в нем военной техники, было также убито и ранено 315 солдат и офицеров из частей 
  РОА [37].<br>
  <br>
  Кто же шел добровольцем в эту армию? Для советской стороны были характерны такие 
  высказывания: «По Симферополю еще набрали в РОА около 15 человек, и то из числа 
  уголовников и других враждебных элементов, подвергшихся репрессиям со стороны 
  советской власти» [38]. Или, как писала газета «Красный Крым», в РОА «удалось 
  собрать 6—7 человек, под угрозой расстрела» [39]. Как известно, такие высказывания 
  были характерны не только для крымской ситуации.<br>
  <br>
  Правдой в них было только то, что в РОА действительно шли люди, которые были 
  не в ладах с советской властью и зачастую не по своей вине. Об одном из них, 
  В.И. Мальцеве, говорилось выше. Покинув в сентябре 1943 г. Крым, он попал в 
  Русскую авиационную группу в Восточной Пруссии. Осенью 1944 г. на основе этой 
  группы стали формироваться ВВС Комитета освобождения народов России, командующим 
  которыми в чине генерал-майора и стал Мальцев [40].<br>
  <br>
  В июне 1943 г. добровольцем в РОА вступил феодосийский бургомистр И.С. Харченко. 
  А вскоре этому примеру последовали и многие другие представители местной гражданской 
  администрации. Однако основным контингентом, который шел в РОА, были простые 
  местные жители и военнопленные, доведенные до отчаяния в немецких концлагерях 
  [41].<br>
  <br>
  Из вышесказанного видно, что источники пополнения крымских формирований РОА 
  были разнообразными, разнообразной была и униформа их личного состава. Так, 
  бойцы частей РОА, набранные непосредственно на территории полуострова, носили 
  в основном немецкую униформу пехотного образца, выданную со складов вермахта. 
  Офицеры-пропагандисты прибывали из Дабендорфа, как правило, в своей униформе: 
  или немецкой, или трофейной со складов иностранных армий (например, голландской). 
  В ноябре-декабре 1943 г. в Крым с Таманского полуострова стали прибывать части 
  17-й немецкой армии. Находившиеся в ее составе «батальоны и роты РОА» не имели 
  единого обмундирования. Большинство солдат этих формирований носили немецкую 
  пехотную униформу или одежду, специально перешитую по военному образцу.<br>
  <br>
  Единственным, что объединяло все виды этого обмундирования, была символика и 
  знаки различия РОА. Как известно, они начали разрабатываться с января-февраля 
  1943 г., а уже 4 апреля 1943 г. газета «Доброволец» опубликовала образцы и описание 
  этих знаков, включавших:<br>
  <br>
  1. Нарукавную эмблему в виде щитка темно-зеленого цвета с синим андреевским 
  крестом на белом поле с красной окантовкой и желтыми буквами «РОА» в верхней 
  части щитка.<br>
  <br>
  2. Овальную сине-красную кокарду.<br>
  <br>
  3. Темно-зеленые петлицы с продольной полоской и пуговицей, с серебряной выпушкой 
  для офицеров и золотой для генералов.<br>
  <br>
  4. Темно-зеленые погоны с красной выпушкой, с белыми суконными лычками для унтер-офицеров, 
  с красными просветами и золотистыми звездочками для офицеров и с золотыми зигзагообразными 
  галунами для генералов.<br>
  <br>
  Курсанты школы пропагандистов в Дабендорфе одними из первых получили новые петлицы, 
  погоны и эмблемы РОА. Вероятно, с прибытием этих офицеров в Крым и связано первое 
  появление знаков различия РОА на полуострове [42].<br>
  <br>
  На вооружении крымских формирований РОА (как, впрочем, и в других регионах) 
  состояли, главным образом, трофейные образцы пехотного вооружения: советские, 
  польские, чехословацкие и др. винтовки, автоматы и пулеметы. Когда же трофейных 
  образцов не хватало, то в качестве исключения допускалось вооружение некоторых 
  частей (самых надежных) немецким оружием. В основном это были карабины Маузера 
  98k, автоматы MP-40 и пулеметы MG-34 и MG-42. Что касается тяжелого пехотного 
  вооружения (минометы, противотанковые средства) и артиллерии, то их на вооружении 
  крымских формирований РОА не было.<br>
