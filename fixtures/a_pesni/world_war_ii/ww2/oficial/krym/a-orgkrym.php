	<br>
	<strong>Олег Романько, кандидат исторических наук, доцент кафедры украиноведения Крымского 
  государственного медицинского университета им. С.И. Георгиевского (Симферополь)</strong><br>
  <br>
<span class="header"><strong>ОРГАНЫ УПРАВЛЕНИЯ НА ОККУПИРОВАННОЙ ТЕРРИТОРИИ КРЫМА 
  (1941—1944) </strong><br></span> 
  <br>
  <em><a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=91&par=74&l=&art=473" target="_blank">Журнал &quot;Историческое наследие Крыма&quot;, №11, 2005</a>.</em><br>
  <br>
  <br>
  В годы Второй мировой войны Крым играл исключительно большое значение в планах 
  руководства Третьего рейха. В силу целого ряда причин как политического, так 
  и военного характера для захвата и удержания полуострова было затрачено значительное 
  количество людских и материальных ресурсов. Фактически борьба за Крым продолжалась 
  на протяжении почти трех лет, которые условно можно разделить на два периода:<br>
  <br>
  • активные боевые действия регулярных частей Вермахта и Красной Армии (октябрь 
  1941 — июль 1942 г. и апрель — май 1944 г.);<br>
  <br>
  • оккупация (декабрь 1941 (фактически август 1942 г.) — апрель 1944 г.), когда 
  основным противником немецких силовых структур на полуострове были партизаны 
  [1].<br>
  <br>
  О первом периоде сказано уже много, и описан он достаточно полно. Второй же 
  в литературе представлен крайне односторонне. В советских исторических описаниях 
  весь этот период оккупации неизменно сводился к зверствам немцев над мирным 
  крымским населением, экономическому ограблению полуострова и партизанской войне. 
  При этом оккупанты, которые почти два года являлись полными хозяевами в Крыму, 
  представлялись советскими историками очень поверхностно. Тогда как известно, 
  что на любой занятой территории немцы со всей свойственной им педантичностью 
  учреждали разветвленный оккупационный аппарат, у каждой из частей которого была 
  своя сфера влияния.<br>
  <br>
  Одной из главных задач любого оккупационного режима является умиротворение подведомственной 
  ему территории. Тем более такой территории, как Крым, который даже после окончания 
  здесь активных боевых действий являлся ближним тылом немецких армий, наступающих 
  на Сталинград и Кавказ. Другим важным, с военной точки зрения, фактором следует 
  признать партизанское движение. Оно не приобрело здесь таких масштабов, как, 
  например, в Белоруссии или Брянских лесах. Но и не признавать партизанское движение 
  реальной угрозой немцы не могли. Дело в том, что все районы дислокации партизан 
  находились в непосредственной близости от населенных пунктов и важных коммуникаций. 
  Да и «большая земля», с которой они получали помощь, была совсем близко. Поэтому 
  закономерно, что на полуострове находилось значительное количество немецких 
  военно-административных и полицейских органов всех типов, об организации, структуре 
  и функциях которых наш читатель также имеет далеко не полное представление.<br>
  <br>
  Наконец, еще одной, самой малоизученной страницей истории оккупации Крыма является 
  сотрудничество местного населения с немецким оккупационным режимом, или проблема 
  коллаборационизма. Это явление имеет много разновидностей, для нас наиболее 
  интересной является одна из самых активных форм — политическая. Последняя подразумевает 
  участие представителей местного населения в работе органов самоуправления или 
  подобных им структур, которые создавались оккупантами. Эта проблема является 
  сложной для объективного понимания. В Крыму же она приобрела еще большую остроту 
  в силу многонациональности региона.<br>
  <br>
  Главной особенностью немецкого оккупационного режима на территории СССР было 
  то, что он только в теории представлял единый институт, управляемый из Берлина. 
  На деле же этот режим состоял из трех практически автономных, но и взаимопересекающихся 
  (территориально и административно) ветвей власти: гражданской администрации, 
  представленной органами Министерства по делам оккупированных восточных областей, 
  различных военных оккупационных инстанций и аппарата полиции и СС. Не был исключением 
  и Крым, на территории которого немецкая оккупационная администрация складывалась 
  следующим образом [2].<br>
  <br>
  20 июня 1941 г. в Берлине состоялось совещание высшего военно-политического 
  руководства Германии, где Гитлеру был представлен новый план будущего административно-политического 
  устройства того, что должно было остаться от СССР. Согласно этому плану создавалось 
  пять административных единиц — рейхскомиссариатов (Reichskomissariat):<br>
  <br>
  • «Московия» (центральные области России);<br>
  <br>
  • «Остланд» (Прибалтика и Белоруссия);<br>
  <br>
  • «Украина» (большая часть Украины и Крым);<br>
  <br>
  • «Кавказ» (Северный Кавказ, Закавказье и Калмыкия);<br>
  <br>
  • «Туркестан» (Средняя Азия, Казахстан, Поволжье и Башкирия).<br>
  <br>
  Эти административные единицы планировалось создавать по мере продвижения линии 
  фронта на Восток и после умиротворения указанных регионов [3].<br>
  <br>
  Гитлер согласился с таким решением проблемы и уже 17 июля 1941 г. подписал приказ 
  о введении гражданского управления на оккупированных советских территориях. 
  Согласно этому приказу возникло Министерство оккупированных восточных областей 
  (Reichsministerium für die besetzen Ostgebiete) — главный руководящий орган 
  указанных территориальных образований. Возглавил министерство видный нацистский 
  теоретик и специалист по «восточному вопросу» Альфред Розенберг. Однако вследствие 
  провала планов «молниеносной войны» против СССР удалось создать только два рейхскомиссариата 
  — «Остланд» и «Украина». Они начали функционировать 1 сентября 1941 г. Окончательно 
  же их территории оформились только к декабрю 1941 г. [4].<br>
  <br>
  Согласно планам министерства Розенберга Крым вместе с территорией Херсонской 
  и Запорожской областей должен был войти в генеральный округ «Таврия» (Generalbezirk 
  Taurien) с общей площадью 22900 км<sup>2</sup> и населением 661981 чел. (по 
  состоянию на 1 сентября 1941 г.). Центром округа становился г. Мелитополь. Генеральный 
  округ «Таврия» являлся составной частью рейхскомиссариата «Украина» (Reichskomissariat 
  Ukraine)*. Помимо него в эту административную единицу должны были войти генеральные 
  округа «Волыния-Подолия», «Житомир», «Киев», «Николаев» и «Днепропетровск» [5].<br>
  <br>
  <hr>
  <em>* Во главе рейхскомиссариата «Украина» стоял видный функционер нацистской 
  партии Эрих Кох. Его резиденция находилась в г. Ровно.</em><br>
  <hr>
  <br>
  Высшим органом гражданской оккупационной администрации в генеральном округе 
  «Таврия» должен был стать генеральный комиссариат, возглавить который поручалось 
  Альфреду Фрауэнфельду. Однако вследствие того, что до лета 1942 г. подведомственная 
  ему территория являлась ближним тылом действующей армии, к своим обязанностям 
  он смог приступить только 1 сентября 1942 г. К этой дате относится окончательное 
  оформление территории генерального округа «Таврия», каким его планировалось 
  учредить в министерстве Розенберга. Но территория Крыма так и не перешла под 
  юрисдикцию генерального комиссара Фрауэнфельда. По указанным выше причинам полуостров 
  считался находящимся под двойным управлением: гражданским (номинально) и военным 
  (фактически). То есть из состава генерального округа Крым никто не изымал, однако 
  гражданские чиновники не имели здесь никакой власти. Реальная власть на полуострове 
  принадлежала командующему расквартированных здесь частей Вермахта [6].<br>
  <br>
  Во главе местного аппарата военной администрации находился так называемый командующий 
  войсками Вермахта в Крыму (Befehlshaber Krim), который по вертикали подчинялся 
  командующему группой армий «А» (с апреля 1944 г. — группа армий «Южная Украина»). 
  Обычно такая должность вводилась на тех оккупированных территориях, где высший 
  начальник Вермахта должен был не только осуществлять охранную службу, но еще 
  и заниматься их административным устройством [7].<br>
  <br>
  С декабря 1941 по май 1942 г. в этой должности находился командующий 337-й пехотной 
  дивизией генерал-лейтенант Карл Шпанг*.<br>
  <br>
  <hr>
  <em>* В этот период его должность называлась командующий войсками Вермахта на 
  полуострове Крым (Befehlshaber der landengen der Halbinsel Krim).</em><br>
  <hr>
  <br>
  Однако его власть была, скорее, номинальной и сводилась больше к административным 
  функциям, так как до августа 1942 г. на территории Крыма еще находилась 11-я 
  немецкая армия генерал-фельдмаршала Эриха фон Манштейна, части и соединения 
  которой и обеспечивали охрану полуострова. Ему же принадлежала и верховная гражданская 
  власть. В августе — сентябре 1942 г. 11-я немецкая армия покинула Крым. В связи 
  с этим возникла необходимость в создании реального органа, который стал бы здесь 
  главной военно-административной инстанцией. С этой целью и была введена должность 
  командующего войсками Вермахта в Крыму, которую последовательно занимали следующие 
  лица:<br>
