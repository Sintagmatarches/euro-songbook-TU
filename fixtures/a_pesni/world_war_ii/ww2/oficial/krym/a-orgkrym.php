  Не был исключением и аппарат фюрера СС и полиции генерального округа «Таврия» 
  (SSPf Taurien)*<br>
  <br>
  <hr>
  <em>* В некоторых источниках название этой должности звучит следующим образом: 
  фюрер СС и полиции «Таврия-Крым-Симферополь» (SSPf Taurien-Krim-Simferopol).</em><br>
  <hr>
  <br>
  Организационно ему должны были подчиняться:<br>
  <br>
  • начальник полиции безопасности и СД генерального округа «Таврия» (Kommandeur 
  der Sicherheitspolizei und SD Taurien). Ему, в свою очередь, были подвластны 
  местные начальники гестапо, СД и криминальной полиции. С ноября 1941 г. эту 
  должность последовательно занимали: СС-штандартенфюрер Отто Олендорф (до июля 
  1942 г.), СС-оберштурмбаннфюрер Пауль Цапп (июль 1942 — май 1943 г.) и СС-оберфюрер 
  Гейнц Рох (май 1943 — апрель 1944 г.);<br>
  <br>
  • начальник полиции порядка генерального округа «Таврия» (Kommandeur der Ordnungspolizei 
  Taurien). Ему подчинялись местные начальники охранной полиции, жандармерии, 
  железнодорожной охраны, а позднее и вспомогательной полиции порядка, набранной 
  из местных добровольцев. С августа 1942 г. и до самого конца оккупации Крыма 
  эту должность занимал генерал-майор полиции порядка Пауль Хитшлер. Столь позднее 
  создание этого поста по сравнению с предыдущим объясняется тем, что до указанного 
  периода функции полиции порядка на полуострове выполняла полевая жандармерия 
  11-й армии [14].<br>
  <br>
  В округах и районах генерального округа «Таврия» находились структурные подразделения 
  аппарата фюрера СС и полиции, которые возглавляли окружные и районные фюреры. 
  Всего было пять полицейских округов, которые территориально совпадали с округами 
  гражданской администрации. Охранная полиция и полиция порядка были представлены 
  в этих округах соответствующими отделами [15].<br>
  <br>
  Каждая из двух частей полиции генерального округа «Таврия» имела двойное подчинение. 
  С одной стороны, она подчинялась своему фюреру СС и полиции, а через него — 
  главному фюреру СС и полиции «Россия-Юг». С другой же стороны, она подчинялась 
  соответствующему главному управлению в Берлине. Однако в данном случае это не 
  играло существенной роли, так как единственным начальником всех управлений СС 
  и полиции был Гиммлер.<br>
  <br>
  Другой особенностью полицейского аппарата на оккупированной советской территории 
  являлось то, что он был менее структурированным, чем в Германии. Сказывался 
  недостаток профессиональных кадров. В связи с этим оккупанты были вынуждены 
  создавать комбинированные полицейские органы. То есть сотрудники полиции безопасности 
  и СД выполняли одновременно функции и гестапо, и криминальной полиции. Так же 
  обстояло дело и в сфере компетенции полиции порядка [16].<br>
  <br>
  В конце 1943 г. полицейский аппарат на Украине подвергся значительной реорганизации. 
  В ноябре 1943 г. в тыловом районе группы армий «А» была создана новая должность 
  — главный фюрер СС и полиции «Черное море» (HSSPf Schwarzes Meer), в подчинение 
  которого вошли фюреры СС и полиции «Таврии» и «Николаева». На этот пост со штаб-квартирой 
  в Николаеве был назначен уже СС-группенфюрер Людольф фон Альвенслебен. Аппарат 
  главного фюрера СС и полиции «Россия-Юг» продолжал существовать параллельно, 
  исполняя свои обязанности только в зоне ответственности гражданской администрации. 
  В марте 1944 г. в связи со значительным сокращением рейхскомиссариата «Украина» 
  на его территории и в тыловом районе группы армий «Южная Украина» был создан 
  единый полицейский аппарат, руководитель которого СС-обергруппенфюрер Ханс Прютцманн 
  стал теперь называться верховный фюрер СС и полиции «Украина» (Hüchste SSPf 
  Ukraine). Однако, несмотря на эту унификацию, все прежние полицейские должности 
  сохранялись и для Крыма вышестоящей инстанцией по-прежнему оставался главный 
  фюрер СС и полиции «Черное море» [17].<br>
  <br>
  Со временем каждая из ветвей немецкой оккупационной администрации стала привлекать 
  к сотрудничеству население оккупированного Крыма. В политической сфере первоначально 
  это выражалось в создании и функционировании органов так называемого местного 
  самоуправления: сельских, районных и городских управлений. Их соответственно 
  возглавляли: старосты, начальники районных и городских управлений. Эти органы 
  создавались сразу же по установлении на данной территории немецкой военной или 
  гражданской администрации. В политическом отношении эти органы самоуправления 
  были абсолютно пассивны и бесправны, а их руководители полностью подчинялись 
  соответствующим немецким чиновникам: окружным, районным или городским комиссарам. 
  Если же такие органы самоуправления создавались в зоне действия военной администрации, 
  то их руководители подчинялись шефам полевых или местных комендатур.<br>
  <br>
  В руках начальника районного управления находилось общее руководство районом. 
  Он нес политическую ответственность за все подчиненные ему местные учреждения, 
  хозяйство и управления, обеспечивал «покой и порядок» на подведомственной территории, 
  боролся с проявлениями саботажа, диверсиями, неподчинением оккупационным властям, 
  организовывал «изъятие» продукции для нужд Германии и удовлетворял потребности 
  подразделений Вермахта, которые были расквартированы на территории его района.<br>
  <br>
  Руководитель района назначался и увольнялся с должности по предложению полевой 
  комендатуры, командующего тыловым районом армии или группы армий, а в генеральном 
  комиссариате — коменданта местной комендатуры или районного комиссара. Структура 
  районного управления предусматривала основные отделы: общего управления, вспомогательной 
  полиции порядка, школ и культурных учреждений, охраны здоровья, ветеринарный, 
  финансовый, строительства, промышленности, снабжения и обеспечения рабочей силой. 
  Со временем появился еще один отдел — пропаганды. Их руководители назначались 
  обычно начальником районного управления по согласованию с местным военным или 
  гражданским немецким начальником [18].<br>
  <br>
  Следующей по значимости фигурой органов местного самоуправления являлся бургомистр. 
  В данном случае это понятие имело два значения:<br>
  <br>
  • руководитель общинного управления (обычно бывший советский сельский совет, 
  состоявший из нескольких сел);<br>
  <br>
  • руководитель городского управления, который подчинялся начальнику соответствующего 
  районного управления.<br>
  <br>
  На своей территории задачи бургомистра были абсолютно те же, что и у начальника 
  районного управления. Таким же был порядок назначения и увольнения с занимаемой 
  должности. Аппарат бургомистра состоял из тех же отделов, что и районное управление.<br>
  <br>
  Низшей инстанцией местного самоуправления было сельское управление, во главе 
  которого стоял староста. Последнего, как правило, назначал бургомистр общины. 
  Практическая работа сельских управлений сводилась нередко к обычной бухгалтерской 
  рутине. Однако в большинстве случаев, и это характерно для Крыма, вследствие 
  трудностей со связью, протяженностью территории и главное, активным сопротивлением 
  населения оккупационной политике, работа в селах часто выходила за рамки предусмотренного 
  объема. Дела сельского управления требовали зачастую приложения таких усилий, 
  что в большинстве районов немцам пришлось выплачивать старостам зарплату. Изначально 
  же они работали на общественных началах [19].<br>
  <br>
  Староста со своим помощником, бухгалтером и подчиненными управлению полицейскими 
  должен был проводить в жизнь все распоряжения немецкой администрации, бургомистра 
  и начальника районного управления. Например, в их задачи входила регистрация 
  прибывших, учет местного населения, сбор налогов, обеспечение поставок для частей 
  Вермахта, предоставление рабочей силы, гужевого транспорта, квартир для воинских 
  частей и т. п. [20].<br>
  <br>
  Как правило, на должности начальников местного самоуправления всех уровней назначались 
  люди, которые уже зарекомендовали себя «политически благонадежными» и активными 
  пособниками оккупантов. Так, например, на должность ялтинского бургомистра был 
  назначен Виктор Мальцев, бывший полковник советских ВВС, который в довоенный 
  период подвергался репрессиям. При назначении на должность эти лица должны были 
  пройти проверку полиции безопасности и СД (в гражданской зоне) или тайной полевой 
  полиции (в военной зоне). Однако и в дальнейшем все эти люди продолжали находиться 
  под наблюдением тех или иных немецких органов. Что же касается комплектования 
  исполнительного аппарата управлений (отделов), то немцы старались набрать в 
  них сотрудников, которые уже были знакомы с работой подобных органов. Поэтому 
  нет ничего удивительного, что в этих отделах осталось много бывших советских 
  служащих, сочувствующих прежней власти.<br>
  <br>
  Выше уже было сказано, что одной из особенностей коллаборационизма на территории 
  Крыма является его национальная окраска. Это проявилось в создании так называемых 
  национальных комитетов. Так, в течение 1941—1942 гг. на полуострове появились 
  татарские, армянские, болгарские, украинские и другие комитеты. Эти комитеты 
  организовывались синхронно с органами самоуправления, но они не являлись параллельной 
  им властью (хотя многие национальные лидеры и претендовали на это). Они даже 
  не являлись властью вообще, и в том урезанном виде, какой имели районные, городские 
  и сельские управления. Скорее, это были представительные органы, так как их 
  основной задачей являлось отстаивание интересов данной национальной группы (или 
  влияние на ее членов в нужном оккупантам духе). Культурные, религиозные, экономические, 
  но ни в коем случае не политические интересы. Еще одним отличием от органов 
  местного самоуправления, подчиненным военной администрации, было то, что вся 
  их деятельность направлялась и контролировалась полицией безопасности и СД. 
  Финансирование органов местного самоуправления происходило за счет собранных 
  с населения налогов (т. е. из оккупационного бюджета). Национальные комитеты 
  должны были содержаться исключительно на средства, собранные от деятельности 
  подведомственных учреждений культуры (например, театров, музыкальных ансамблей 
  и т. п.), а также за счет добровольных пожертвований. Заниматься какой-либо 
  предпринимательской деятельностью членам комитета было запрещено. Наконец, о 
  том, что национальные комитеты не должны были подменять местное самоуправление 
  свидетельствует и тот факт, что руководителями последнего могли стать представители 
  любой населявшей Крым национальной группы (например, в татарской деревне старостой 
  вполне мог быть татарин). Тогда как членом комитета мог стать представитель 
  только определенной национальности [21].<br>
  <br>
  В целом, ни одна из национальных групп на территории Крыма не смогла получить 
  для своего комитета сколько-нибудь серьезного политического статуса. У некоторых 
  из них, например симферопольского украинского комитета, даже не было возможности 
  создать свои филиалы в других городах и районах полуострова — его деятельность 
  так и ограничилась столицей Крыма. На наш взгляд, этому было несколько причин. 
  Во-первых, несмотря на то, что в войне с СССР немцы очень активно планировали 
  разыграть национальную карту, их реальная политика в этом вопросе отличалась 
  большой осторожностью. Не секрет, что, например, украинское национальное движение 
  преследовалось ими до начала 1944 г. наравне с коммунистическим подпольем. Поэтому 
  естественно, что в таком стратегически важном регионе, как Крым, они никогда 
  не позволили бы ему развернуться в полную силу. Во-вторых, многим комитетам 
  просто некого было представлять. Тот же украинский комитет из-за отсутствия 
  ясной идеологической платформы вряд ли имел шансы быть понятым местным населением. 
  Поэтому его членам только и оставалось, как привлекать «украинцев» продажей 
  муки и других продуктов питания из специального «украинского магазина». И, наконец, 
  в-третьих, после коренного перелома на Восточном фронте все меньше и меньше 
  населения хотело связывать свою судьбу с подобными организациями. И это начиная 
  с весны 1943 г. общая тенденция для всех оккупированных территорий СССР [22].<br>
  <br>
  Наиболее показательной является история татарских национальных комитетов. В 
  силу разного рода обстоятельств они смогли добиться от немцев больше, чем все 
  остальные национальные комитеты вместе взятые. Однако и вышеуказанные причины 
  общего регресса отразились на этих организациях наиболее рельефно.<br>
  <br>
  Начало создания татарских комитетов на территории Крыма относится к концу 1941 
  — началу 1942 г. Так, уже в конце декабря 1941 г. в Бахчисарае был создан первый 
  Мусульманский комитет, а затем на его основе — комитет в Симферополе. По замыслу 
  его основателей — председателя Джемиля Абдурешидова и двух его заместителей 
  Ильми Керменчиклы и Османа Меметова — этот комитет должен был представлять всех 
  крымских татар и определять все сферы их жизни. Однако начальник полиции безопасности 
  и СД, при поддержке которого был создан этот орган, сразу же запретил им называть 
  комитет «крымским», оставив в его названии только слово «симферопольский». В 
  этом качестве он должен был служить примером только районным мусульманским комитетам, 
  которые стали создаваться в других городах и населенных пунктах Крыма в январе 
  — марте 1942 г. [23].<br>
  <br>
  Симферопольский мусульманский комитет состоял из 18 человек: президента, двух 
  его заместителей и пятнадцати членов, каждый из которых отвечал за определенную 
  сферу деятельности. Все члены комитета в обязательном порядке утверждались начальником 
  полиции безопасности и СД.<br>
  <br>
  Согласно уставу главной целью создания комитета было содействие во всех вопросах 
  немецкой оккупационной администрации. Решением же каждого конкретного вопроса 
  занимался соответствующий отдел, которых в структуре комитета было создано шесть:<br>
  <br>
  • по борьбе с бандитами (т. е. с партизанами);<br>
  <br>
  •по комплектованию добровольческих формирований;<br>
  <br>
  • по оказанию помощи семьям добровольцев;<br>
  <br>
  • культуры и религии;<br>
  <br>
  • пропаганды и агитации;<br>
  <br>
  • административно-хозяйственный [24].<br>
  <br>
  Все районные мусульманские комитеты имели такую же структуру и в своих действиях 
  руководствовались указаниями симферопольского комитета.<br>
  <br>
  Несмотря на полное подчинение всей деятельности комитетов немецкой оккупационной 
  администрации, лидеры крымскотатарских националистов не оставляли надежды получить 
  более широкие полномочия, вплоть до провозглашения в Крыму татарского государства. 
  Так, в апреле 1942 г. группа руководителей симферопольского комитета разработала 
  новый устав и программу деятельности этого органа. Выдвигались главные требования, 
  предполагавшие создание:<br>
  <br>
  • татарского парламента;<br>
  <br>
  • татарской национальной армии;<br>
  <br>
  • самостоятельного татарского государства под протекторатом Германии.<br>
  <br>
  Эта программа была подана на рассмотрение Гитлеру, однако он ее не одобрил [25].<br>
  <br>
  В мае 1943 г. один из старейших крымскотатарских националистов Амет Озенбашлы 
  написал на имя Гитлера меморандум, в котором изложил программу сотрудничества 
  между Германией и крымскими татарами, основные положения которой были сходны 
  с пунктами предыдущей. Однако выполнение подобных требований не входило в планы 
  нацистского руководства, поэтому начальник полиции безопасности и СД счел «более 
  благоразумным» не давать ход этому документу. Гитлер о нем так и не узнал [26].<br>
  <br>
  Все эти попытки были обречены на неудачу, так как «оккупанты повсюду твердо 
  держали власть в своих руках и свирепо подавляли малейшие попытки к обретению… 
  национальной самостоятельности» [27]. В сложившихся военно-политических условиях 
  иначе быть и не могло. Поэтому уже к концу 1943 г. почти все мусульманские районные 
  комитеты практически не функционировали. Так, даже симферопольский комитет реально 
  состоял только из одного человека — своего председателя Абдурешидова. Хотя помимо 
  него в комитете на тот момент числилось еще 11 членов, ни один из них участия 
  в его деятельности не принимал [28].<br>
  <br>
  В начале 1944 г. немцы предприняли запоздалую попытку объединить усилия по использованию 
  политического коллаборационизма. В январе этого года командующий немецкими войсками 
  в Крыму генерал-полковник Эрвин Йенеке приказал начать подготовку к созданию 
  на полуострове местного правительства (Landesregierung). По замыслу генерала 
  оно должно было состоять из представителей трех основных национальностей, населяющих 
  Крым: татар, русских и украинцев (именно в таком порядке они перечислены в отчете 
  Йенеке командованию группы армий «А»). Основой этого правительства должны были 
  послужить органы местного самоуправления и соответствующие национальные комитеты. 
  В его компетенцию, при общем руководстве со стороны немецкой военной администрации, 
  планировалось передать:<br>
  <br>
  • административное руководство органами гражданской власти, а также командование 
  частями вспомогательной полиции;<br>
  <br>
  • все вопросы, касающиеся религии и просвещения;<br>
  <br>
  • благотворительность;<br>
  <br>
  • судопроизводство.<br>
  <br>
  К марту 1944 г. местная администрация в основном была переформирована согласно 
  этому плану. Однако этим замыслам немецкого командования так и не суждено было 
  исполниться, так как уже в апреле — мае 1944 г. Крым был полностью освобожден 
  частями Красной Армии [29].<br>
  <br>
  Изучая немецкий оккупационный режим на территории Крыма, у исследователя может 
  сложиться представление о его громоздкости из-за обилия пересекающихся структур 
  власти. Однако следует подчеркнуть, что между двумя его основными ветвями — 
  военной и полицейской — не было никакой принципиальной разницы, а имелись лишь 
  некоторые, чисто функциональные различия. В действительности же эти ветви представляли 
  собой неразрывно связанные части одного оккупационного аппарата, вся деятельность 
  которого была направлена на максимальное умиротворение полуострова.<br>
