  <span class="text"><br>
  В октябре 1943 г. после эвакуации Кубанского плацдарма на территорию Крыма были 
  выведены части и соединения 17-й немецкой армии. В ноябре 1943 г., после завершения 
  передислокации армии, ее командующий генерал-полковник Эрвин Йенеке занял одновременно 
  и высший административный пост на полуострове (1 мая 1944 г., уже почти на исходе 
  боев за Крым, его сменил генерал пехоты Карл Альмендингер, до этого командующий 
  5-м армейским корпусом) [8].<br>
  <br>
  С целью осуществления всех полномочий при должности командующего войсками Вермахта 
  в Крыму был создан штаб, основой которого послужили соответствующие структуры 
  42-го армейского корпуса 11-й армии. Организационно этот штаб состоял из нескольких 
  отделов, главными из которых были оперативный (I) и административный (VII) отделы. 
  Через первый отдел шло управление охранными войсками [9].<br>
  <br>
  Начальник седьмого отдела руководил военно-административными органами, которые 
  состояли из полевых (Feldkommandantur; FK) и местных комендатур (Ortskommandantur; 
  OK) и наделялись всей полнотой власти в зоне своего действия. Полевые комендатуры 
  создавались обычно в пределах района (например, в Крыму). Им подчинялись местные 
  комендатуры, создаваемые в городах, районных центрах, крупных узлах железных 
  и шоссейных дорог и местах дислокации военных гарнизонов. Все комендатуры преследовали 
  выполнение двух задач: охранной и управленческой. К первой относилось «обеспечение 
  покоя» в оккупированных районах и охрана тылов действующей армии. Ко второй 
  — создание, руководство и контроль органов местного управления, а также «мобилизация 
  резервов» для ведения войны. Это сводилось к следующим основным функциям: борьба 
  с партизанами; охрана коммуникаций, военных объектов и лагерей военнопленных; 
  разведывательная и контрразведывательная деятельность; ведение пропаганды [10].<br>
  <br>
  Всего же за период с 1941 по 1944 г. на территории Крыма функционировали 4 полевые 
  и 23 местные комендатуры, которые располагались в следующих населенных пунктах 
  [11]:<br>
  <br>
  Наконец, согласно приказу Гитлера от 17 июля 1941 г. на рейхсфюрера СС и шефа 
  германской полиции Генриха Гиммлера было возложено «полицейское обеспечение 
  восточных территорий». Последний назначал главных фюреров СС и полиции (Höhere 
  SS- und Polizeiführer; HSSPf), которые являлись высшими полицейскими чиновниками 
  в рейхскомиссариатах или, по согласованию с военной администрацией, в тыловых 
  районах групп армий. Хотя фюреры СС и полиции формально подчинялись рейхскомиссарам 
  или находились в оперативном подчинении у командующих тыловыми районами групп 
  армий, реальную власть над ними имел только Гиммлер. Этот последний факт означал, 
  что полицейская администрация действовала параллельно и на равных правах с гражданской 
  и военной администрациями [12].<br>
  <br>
  С 23 июня (фактически с сентября) 1941 г. главным фюрером СС и полиции на территории 
  рейхскомиссариата «Украина» являлся СС-группенфюрер Фридрих Еккельн, которого 
  уже 11 декабря сменил СС-обергруппенфюрер Ханс Прютцманн. Эти лица исполняли 
  свои обязанности не только на территории гражданской администрации. По договору 
  между Гиммлером и Верховным командованием Вермахта (ОКВ) они отвечали за полицейское 
  обеспечение также и в тыловом районе южного крыла Восточного фронта. В связи 
  с этим их должность официально именовалась главный фюрер СС и полиции «Россия-Юг» 
  (HSSPf Russland-Sud). В генеральных округах, входивших в состав рейхскомиссариата, 
  ему подчинялись местные фюреры СС и полиции. Так, в генеральном округе «Таврия» 
  эту должность c ноября 1941 г. (со штаб-квартирой в Симферополе) занимал СС-бригадефюрер 
  Людольф фон Альвенслебен [13].<br>
