  <strong>Лев Венедиктов, старший научный сотрудник Керченского историко-культурного 
  заповедника</strong><br>
  <br>
 <span class="header"><strong>«ПО ДАННЫМ НАДЕЖНОГО ИСТОЧНИКА...»: НЕКОТОРЫЕ ОСОБЕННОСТИ 
  РАЗВЕДЫВАТЕЛЬНОГО ОБЕСПЕЧЕНИЯ БОЕВЫХ ДЕЙСТВИЙ В КРЫМУ В ГОДЫ ВЕЛИКОЙ ОТЕЧЕСТВЕННОЙ 
  ВОЙНЫ</strong><br></span>
  <br>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=104&par=74&l=&art=597" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, № 19, 
  2007</em></a>.<br>
  <br>
  <br>
  История боевых действий в Восточном Крыму весной и летом 1942 года неразрывно 
  связана с боевыми эпизодами личного состава разведывательных структур армии 
  и флота и подразделений органов госбезопасности, действовавших во взаимодействии 
  для решения единой задачи: проникнуть в планы немецкого командования по подготовке 
  прорыва на Таманский полуостров и дезинформировать противника относительно возможного 
  нашего июньского десанта в Крым через Керченский пролив.<br>
  <br>
  В задачу автора входит обобщение имеющихся сведений о взаимодействии упомянутых 
  спецслужб в информировании командования войск названного выше участка Северо-Кавказского 
  фронта (далее — СКФ) о намерениях противника. Вместе с тем представляет интерес 
  выбор средств и методов добывания такой информации.<br>
  <br>
  В основу исследования положены материалы из архивов бывшей советской военной 
  разведки, фондов Центрального архива Министерства обороны России (далее — ЦАМО 
  РФ) по разведоперациям общевой-сковых армий СКОР, воспоминания бывших сотрудников 
  спецслужб, сведения из Государственного архива в АР Крым (далее — ГААРК) и Керченского 
  историко-культурного заповедника (далее — КИКЗ). Для полноты обзора использованы 
  материалы из «Военно-исторического журнала» (далее — ВИЖ), из сборников по истории 
  боевых действий в Крыму и других литературных и мемуарных источников.<br>
  <br>
  В интересах нашей страны разведку вели два мощных органа: Народный комиссариат 
  внутренних дел (НКВД) и Главное разведывательное управление (ГРУ) Народного 
  комиссариата обороны. ГРУ и НКВД действовали в тесном контакте, особенно в начале 
  войны 1941–1945 годов [1, с. 434]. Так, например, весной 1942 года чекисты выяснили, 
  что предполагается скорое наступление немцев на Майкоп [2, с. 31]. Но на высшем 
  уровне руководства разведки конкурировали между собой [3, с. 434]. По поступавшей 
  к Сталину информации, такое положение было позитивным, ибо одни источники перекрывались 
  другими.<br>
  <br>
  Но ГРУ и НКВД решали задачи, не совпадающие по объектам заинтересованности. 
  Отметим, что сложившаяся точка зрения, будто каждый разведчик — чекист, неточна. 
  Это относится к сотрудникам органов госбезопасности, в том числе к разведчикам, 
  внедрявшимся в спецслужбы противника, в его политические и государственные органы.<br>
  <br>
  Коротко отметим этапы преобразований в органах военной разведки.<br>
  <br>
  Разведуправление РККА (Рабоче-крестьянской Красной Армии) было образовано в 
  1934 году. В июне 1940-го оно стало именоваться 5-м Управлением РККА. За год 
  до начала войны оно было введено в структуру Генштаба (ГШ) Красной Армии. В 
  феврале 1942 года (по сентябрь 1947 г.) ему был придан статус Главного управления 
  — ГРУ ГШ Красной Армии [4, с. 41]. При разведотделах (РО) штабов приграничных 
  округов, которые с началом боевых действий преобразовались во фронтовые разведотделы, 
  находились оперативные пункты РУ ГШ. Подробно о них будет сказано далее.<br>
  <br>
  Разведотдел фронта, как и РО Армии, состоял из 1-го отделения — общевойсковой 
  разведки, 2-го отделения — информационного и 3-го отделения — агентурной разведки, 
  — которое занималось подбором и подготовкой негласных помощников — маршрутников, 
  осведомителей, вербовщиков, резидентов и связников [5, с. 165].<br>
  <br>
  В вопросе о разведывательном обеспечении боевых действий в Крыму в годы Великой 
  Отечественной войны мы делаем акцент на организации и проведении именно агентурных 
  операций в интересах добывания информации о противнике для своего командования 
  и дезинформации спецслужб немцев о замыслах советского военного руководства 
  в сражениях за наш полуостров в 1942 году.<br>
  <br>
  В настоящей статье автор попытался проследить эффективность наших разведывательных 
  усилий осенью 1941 года, в летние месяцы 1942 года при защите Крымского полуострова, 
  в том числе его восточной части.<br>
  <br>
  Решение командования войск Крыма о проведении противодесантной обороны путем 
  маневрирования наличными силами (а именно это положение закладывалось в основу 
  оборонительного замысла) опиралось на данные разведки штаба Черноморского флота 
  (ЧФ).<br>
  <br>
  Источники сообщали, что в порту Констанца (Румыния) готовится десант — сосредоточено 
  150 десант-ных катеров; через Дарданеллы в Черное море проследовал итальянский 
  флот якобы для высадки десанта в Одессе и Севастополе; из портов Болгарии и 
  Румынии вышли 37 транспортов. Авиация установила на аэродромах Бухареста скопление 
  двухсот шестимоторных транспортных самолетов, пригодных для переброски парашютистов; 
  обнаружено 10 морских транспортов, направляющихся в Крым [6, с. 7–10].<br>
  <br>
  Спустя десятилетия выяснилось, что аналитики разведки штаба ЧФ переоценили тогда 
  возможности немцев овладеть Крымом с моря: у них не имелось на юге ни судов 
  достаточного тоннажа, ни кораблей прикрытия и поддержки с моря [7, с. 12].<br>
  <br>
  В конце августа 1941 года в войсках Крыма обстановку и намерение противника 
  не знали. Авиаразведка была скована «мессершмидтами». До 20 сентября 1941 года 
  у командования не было ясности, какое направление для немцев является главным: 
  Перекопское или Евпаторийское, Чонгарское либо Керченское.<br>
  <br>
  Разведсводка, датированная 23 сентября 1941 года, у командования 51-й Армии 
  тревоги не вызывала. Вот вывод из ее текста: «Противник, продолжая прикрываться 
  на Крымском направлении, проявляет главные усилия на Мелитопольском направлении» 
  [8, с. 58]. Через 6 часов 11-я немецкая армия обрушила на Перекопские позиции 
  нашей 156-й стрелковой дивизии (СД) всю свою мощь и двинулась на Крым. Распыление 
  наших наличных сил обошлось дорого.<br>
  <br>
  На взгляд автора, сложившееся положение во многом объясняется слабостью нашей 
  войсковой и глубинной разведки. Разведотдел (РО) штаба новой 51-й Армии во главе 
  с полковником Шиловым, надо полагать, был сформирован тогда, когда для развертывания 
  зафронтовой агентурной сети у 3-го отделения уже не было достаточного времени 
  — неожиданный бросок немецких войск через Перекопский перешеек сделал невозможным 
  завершить курс подготовки негласных помощников (а для их обучения, судя по известным 
  документам об одиночных разведчиках, действовавших в Крыму, необходимо от 1,5 
  до 2-х месяцев). Были ли они направлены в тыл немецких войск по заданию руководства 
  РО штаба 51-й Армии осенью 1941 года, мы не знаем. Известно, что достаточно 
  успешно в этот период (осень 1941 г.) действовал оперативный состав разведывательного 
  пункта Одесского военного округа (ОдВо), прикомандированный к РО 51-й Армии 
  с началом отхода наших войск с Ишуньских позиций. О работе этого оперативного 
  пункта (ОП) будет изложено далее.<br>
  <br>
  Изученные документы и публикации свидетельствуют, что добывающая служба глубинной 
  разведки квалифицированно была поставлена у моряков ЧФ. Так, разведка ЧФ (руководитель 
  — полковник Нам-галадзе) получила сведения о том, что 26 ноября 1941 года немцы 
  намерены перейти в решительное наступление под Севастополем. Эта информация 
  дала возможность завершить подготовку инженерного обеспечения боевых действий 
  Севастопольского оборонительного района (СОР). Немцы начали штурм 17 декабря 
  1941 года [9, с. 130].<br>
  <br>
  В январе 1942 года образован Крымский фронт (КФ). Начальником РО фронта стал 
  полковой комиссар Капалкин. Начальником 1-го отделения назначен майор Тоноян. 
  В 3-м отделении начальником был подполковник Смирнов. В ходе подготовки наступления 
  с начала весны 1942 года разведотделы КФ и трех общевойсковых армий — 44, 51 
  и 47-й — планировали и осуществляли агентурно-оперативные мероприятия с целью 
  получения разведывательной информации через подготовленных людей из числа местного 
  населения.<br>
  <br>
  Наличие достоверной развединформации о замыслах противника создавало благоприятные 
  условия для действия наших войск в Крыму. Например, фронтовая разведка весной 
  1942 года установила точный день начала перехода немцев к активным действиям 
  против войск КФ. Однако ни представитель Ставки, ни командующий фронтом не приняли 
  надлежащих мер, чтобы отразить удар. 8 мая немцы прорвали наши позиции и стали 
  быстро развивать успех [10, с. 162]. Керчь пала 17–18 мая 1942 года.<br>
  <br>
  С 18 мая наши войска перешли к обороне Таманского побережья. Береговой рубеж 
  по проливу в полосе действий немецких войск, занимавших Керченский район (город 
  и его пригород), обороняли дивизии 47-й Армии СКФ: 77-я СД, 103-я Краснознаменная 
  отдельная стрелковая бригада, 32-я гвардейская СД и воины Керченской военно-морской 
  базы (КВМБ). Боевые действия на этом рубеже в мае—августе 1942 года отличались 
  широким использованием возможностей спецслужб СКФ и 47-й Армии — разведки и 
  органов госбезопасности.<br>
  <br>
  Перед бывшим в то время начальником развед-отдела образованного к 20 мая 1942 
  года Северо-Кавказского фронта полковником Николаем Михайловичем Трусовым (в 
  начале 1943 г. он стал генерал-майором) [11, с. 281] встала задача обеспечить 
  командование достоверной информацией о намерениях немцев. Успех сдерживания 
  их наступления на Кавказ с Крымского плацдарма во многом зависел от определения 
  и упреждения нашим командованием реального начала десантной операции противника 
  через пролив. В то же время спецслужбы фронта за-планировали мероприятия по 
  дезинформации немцев относительно развития нашего контрнаступления в направлении 
  Керченского полуострова, т. е. о возможном десанте войск СКФ с побережья Тамани.<br>
  <br>
  Эта ситуация сложилась в связи с подготовкой Манштейном решительного штурма 
  Севастополя: знание намерений противостоящей стороны давало возможность немцам 
  высвободить войска из района Керчи для усиления группировки под Севастополем, 
  а наша стратегия была направлена на сковывание сил немцев на Керченском полуострове, 
  чтобы облегчить положение севастопольцев, ибо сил и средств для реального десанта 
  в Крым летом 1942 года не было.<br>
  <br>
  Идея о дезинформации немецкого командования относительно планов боевых действий 
  на Юге, и в частности на Таманском полуострове, летом 1942 года прошла утверждение 
  в Ставке ВГК. Руководящие документы о наступлении наших войск с переправой через 
  пролив свидетельствовали о проведении операции во второй половине июня 1942 
  года. Проследим хронологию разрабатываемых боевых приказов на наступление и 
  проводившихся демонстративных действий командования 47-й Армии и взаимодействующих 
  с ней военно-морских сил, подчиненных СКФ. (Архивных документов, содержащих 
  комплексные планы по дезинформации противника в битве за Кавказ и Крым, в руки 
  автора не попадали; возможно, что в ЦАМО РФ таких документов нет и не было, 
  так как они могли разрабатываться в органах фронтовой контрразведки, т. е. по 
  линии НКВД.)<br>
  <br>
  Выполнение основных разведывательно-демонстрационных мероприятий в связи с замыслом 
  вышестоящего руководства было возложено на начальника РО штаба 47-й Армии подполковника 
  Ивана Михайловича Вахрамеева. Его сменил полковник Васильев, с 12 июля 1942 
  года ставший начальником штаба армии и передавший дела подполковнику Баранову 
  [12, с. 50].<br>
  <br>
  Разведотдел 15 мая 1942 года с 9.00 со штабом армии дислоцировался в городе 
  Тамань, в последующем — в Темрюке. В составе РО было 1-е отделение (войсковая 
  разведка, начальник — старший лейтенант Жваков или Жмаков, после него — Размакшин), 
  2-е отделение (информационное, начальник — старший лейтенант Комиссаров), 3-е 
  отделение (глубинная или агентурная разведка, начальник — капитан Злобин).<br>
  <br>
  Армейская разведка, в том числе РО Штарма-47, тесно взаимодействовала с разведподразделением 
  НКВД Крымской АССР. Такой контакт и обмен информацией отмечался еще в боях по 
  удержанию Ак-Монайских позиций и Керченского полуострова весной 1942 года (воспоминания 
  бывшего сотрудника Керченского горотдела НКВД старшего лейтенанта госбезопасности 
  Ивана Александровича Полякова подтверждают это) [13].<br>
  <br>
  Поэтому вполне закономерные факты совместной заброски 4-м отделом НКВД Крыма 
  и РО штаба 47-й Армии глубинных разведчиков, в том числе с целью дезинформации 
  противника. Так, из Темрюка в район Мамы-Русской, по одним данным, в конце мая 
  1942 года, по другим — 14 июня 1942 года была высажена разведгруппа, отправкой 
  которой занимался работник НКВД капитан Громов. В нее входил негласный сотрудник 
  органов госбезопасности Яков Иванович Кацыка, 1894 г. р., уроженец Аджимушкая, 
  заместитель директора совхоза Крымгосрыбтреста. Вероятно, целью его заброски 
  на занятую и тщательно контролируемую противником территорию была и дезинформация 
  в отношении планов нашего командования о мнимом переносе военных действий на 
  Керченский полуостров [14, с. 14]. Не исключено, что при Кацыке имелся документ 
  для гарнизона подземелий Аджимушкая с текстом, якобы свидетельствующим о «координации 
  действий в случае начала наступления войск СКФ в июне 1942 г.». Расчет прост: 
  в случае ареста Кацыки немцы из такого «документа» получают важные сведения 
  о подготовке нашего десанта. Есть много обстоятельств, которые говорят о том, 
  что Кацыку арестовали бы неминуемо. Многие местные жители его знали как красного 
  партизана времен Гражданской войны, работника Ленинского сельсовета, профсоюзного 
  лидера, активиста проведения коллективизаций, как бессменного председателя рабочкома 
  в совхозе. В 1941 году он служил в 20-м истребительном батальоне, в его составе 
  в ноябре 1941 года вошел в число участников Караларского партизанского отряда. 
  Таким образом, любой оказавшийся предателем местный житель мог указать на него 
  как на советского активиста и поставить Кацыку-разведчика на грань провала. 
  17 июня 1942 года его раскрыл напарник по разведгруппе. Можно предположить, 
  что Кацыкой пожертвовали ради внедрения легенды о подготовке нашими войсками 
  десанта в июне-июле 1942 года. Для такого вывода есть основания, а именно объяснительная 
  часть документа из Крымского партархива от 5 февраля 1986 года № 9-Б-99 относительно 
  целей заброски Я. И. Кацыки в тыл противника по заданию органов НКВД (известно, 
  что этот документ в ЦГАК поступил из УКГБ по Крыму после настойчивого обращения 
  в инстанции сына разведчика): «…установить связь с оставшимися в Аджимушкайских 
  и Булганакских каменоломнях частями Красной Армии, собрать сведения о дислокации 
  войск противника, их вооружении, расположении складов, а также о наличии авиации 
  в районе Керчи, о характере оборонительных работ на побережье, минных полях, 
  проволочных заграждениях» [15, с. 74]. Зачем органам НКВД понадобились эти сведения, 
  если в тот период в распоряжении командования СКФ, 47-й Армии и трех ее дивизий 
  (не исключается и РО Керченской военно-морской базы — КВМБ) имелись радиоосведомительская 
  резидентура «Тоня», группа «Николаенко» — Е. К. Танасиенко [16, с. 62], маршрутные 
  группы разведчиков и др.? Участие сотрудника госбезопасности Громова в мероприятии 
  по отправке Кацыки на задание можно объяснить только тем, что именно работники 
  органов НКВД и военной контрразведки СКФ и 47-й Армии совместно с командованием 
  должны были разрабатывать детали упомянутой дезинформации и внедрять легендированный 
  документ (если он создавался) через агентуру.<br>
  <br>
  Еще одна деталь. Мы знаем, что была Директива Ставки от 19 июня 1942 года, на 
  основании которой 28 июня командующий СКФ поставил 47-й Армии задачу переправиться 
  на северное побережье Керченского полуострова, овладев рубежом — мысом Тархан, 
  горой Высокой, высота 140,0; 119,0; Горком, а КВМБ предписывалось обеспечить 
  высадку южнее Керчи 142-го батальона морской пехоты (район мыса Опук) [17, с. 
  58]. Приказ отдан в общих чертах, без указания дат и сроков. Этому шагу командования 
  по активизации действий предшествовала целая серия мероприятий, безусловно приковавших 
  внимание противника и как бы раскрывавших намерения войск СКФ начать переправу.<br>
  <br>
  С конца мая 1942 года на побережье, занятое противником, регулярно забрасывались 
  или намеревались высадиться группы разведчиков из дивизий 47-й Армии. Руководил 
  их подбором и подготовкой и. о. начальника, комиссар РО штаба армии, батальонный 
  комиссар И. Ф. Стеценко. Почти все группы задачу разведки не выполнили и возвратились 
  на свой берег. Так, 29 мая катера КВМБ во главе с капитан-лейтенантом Шермановым 
  не высадили группу, так как среди бойцов оказался один, у которого жена якобы 
  находилась в Керчи; 30 мая старший группы не стал высаживаться из-за незнания 
  своих людей (берег ему якобы тоже был незнаком); 3 июня высадка группы состоялась, 
  но после обстрела вернулась назад; 6 июня группа офицера Иголкина оказалась 
  не готовой для перехода в каменоломни, и он приказал вернуться назад. Все эти 
  выходы в море с указанием причин срыва операции зафиксированы в журнале боевых 
  действий КВМБ [18, с. 32].<br>
  <br>
  Трудно поверить, что эти и другие неудачные вылазки — результат низкой квалификации 
  разведчиков и неспособности Стеценко решать оперативные задачи. Видимо, это 
  были демонстративные действия войск по решению задачи вышестоящего командования, 
  имитирующие повышенный интерес к обороне побережья Керченского полуострова, 
  т. е. непосредственную подготовку к десанту. Из архивного документа — донесения 
  о боевых действиях 47-й Армии — известно, что в течение 13 и 14 июня 1942 года 
  (именно в рассматриваемый период) нашими войсками проводилась демонстрация подготовки 
  к проведению десанта на Керченский полуостров: зажигались костры, передвигался 
  личный состав и техника [19, с. 70–73].<br>
  <br>
  Гибли люди и суда, допускались жертвы: ведь операция проводилась на полном серьезе, 
  об иной, секретной цели выхода в море никто из личного состава не должен был 
  догадываться [20, с. 65]. То, что противник мог наблюдать и фиксировать (в том 
  числе при допросе захваченных наших участников войсковых групп либо одиночных 
  разведчиков), должно было укреплять его уверенность в наших намерениях осуществить 
  крупный десант. О практике заброски людей через пролив на немецкую сторону с 
  расчетом на их нестойкость при задержании противником и выдачу «известных» сведений 
  о подготовке десанта повествуется в воспоминаниях одного из офицеров РО штаба 
  47-й Армии [21, с. 1–16].<br>
  <br>
  Такое же убедительное для противника сосредоточение плавсредств предполагалась 
  осуществить нашим командованием. Имелись ли в наличие суда, пригодные для переброски 
  войск с Таманского побережья на Керченский полуостров и сосредоточение которых 
  в заливах, гаванях и бухтах и на пристанях Кавказского берега могла наблюдать 
  немецкая авиаразведка? Документы ГААРК свидетельствуют, что по состоянию на 
  27 мая 1942 года только в подчинении Крымской базы Управления военизированной 
  флотилии (УВФ) НКРП, эвакуированной на Кубань, на ходу было 37 малых судов (а 
  всего — 57), из них поднятых к этому времени со дна — 47, в том числе отремонтированных 
  — 28; гребных средств — 310 единиц. Значительное число плавсредств находилось 
  на Темрюкской базе УВФ [22, с. 198]. Таким образом, немцы вполне могли сопоставить 
  скопление плавсредств на Кавказском побережье в конце мая — начале июня 1942 
  года с другими признаками активизации войск СКФ, чтобы сделать вывод о «намечаемом» 
  десанте. Известно, что именно в эти дни авиация противника совершила «звездный» 
  налет на все гавани и места стоянки малых судов, одновременно разбомбив их. 
  Немцы поверили в легенду, созданную нашим командованием. По разведсводке РО 
  Штарма-47 за 2 июня 1942 года известно, что в Керчь в эти дни прибыло до трех 
  полков румынской пехоты; ранее, 29 мая 1942 года, сюда были переброшены два 
  полка румынской мотопехоты. К 6 июня 1942 года противник огородил берега в районе 
  Керчи колючей проволокой, заминировал береговую полосу, забаррикадировал приморские 
  улицы.<br>
  <br>
  В сводке за 20 июня 1942 года РО КВМБ констатируется, что «в результате ложных 
  демонстраций нашего десанта в период с 15 по 18.06.1942 г. противник перебросил 
  некоторые части на Керченский полуостров и усилил оборону» [23, с. 58, 62, 64, 
  70, 73]. Тем самым ослабилось давление на Севастополь.<br>
  <br>
  Объединяются ли упомянутые выше мероприятия советского командования в некий 
  единый дезинформационный комплекс? Ответ получается положительным.<br>
  <br>
  Об этом свидетельствует хронология цепи событий: в конце мая — начале июня 1942 
  года осуществляется посылка разведгрупп; 13 и 14 июня проводится демонстрационная 
  активизация наших войск; 14–16 июня делается высадка негласного сотрудника органов 
  НКВД; 15–18 июня выполняется отправка разведотрядов для захвата «языка» и обнаружения 
  заготовленных переправочных средств противника.<br>
  <br>
  Как свидетельство верности обобщения приведем выдержку одной из сводок, ежедневно 
  поступавших в штаб СКФ от начальника РО Штарм-47. В них, как правило, содержался 
  вывод об обстановке и возможных действиях противника. «…Вывод из итогов разведсводок 
  с 13 по 16.06.1942 г.: демонстрационные действия наших войск заставили противника 
  усилить обороноспособность частей, обороняющих восточное побережье Керченского 
  полуострова, подтянуть ближайшие резервы» [24, с. 81].<br>
  <br>
  Таким образом, одной из особенностей разведывательного обеспечения боевых действий 
  в Крыму летом 1942 года является участие разведслужбы в мероприятиях по дезинформации 
  противника. Но эта операция имела смысл лишь в период третьего штурма Севастополя.<br>
  <br>
  Отметим тактическую обстановку ко 2 сентября 1942 года, со времени оставления 
  дивизиями Крымфронта восточной оконечности Керченского полуострова 19 мая 1942 
  года до момента, когда немецкие части десантировались через пролив на Кавказское 
  побережье.<br>
  <br>
  После выхода к проливу противник для обороны Керченского полуострова оставил: 
  на северном побережье — 19-ю пд (р), на восточном — 46-ю пд (без 72-го и 97-го 
  пп), 10-ю пд (р) без 33-го пп; на южном — 8-ю пд (р). Остальные части перебросил 
  под Севастополь, сосредоточив здесь до 14 дивизий.<br>
  <br>
  2 июня 1942 года немцы начали третье наступление на СОР, закончившееся 30 июня 
  выходом на мыс Херсонес.<br>
  <br>
  С прорывом обороны Юго-Западного и Южного фронтов летом 1942 года и выходом 
  на Дон и Кубань противник предпринял десантную операцию с Керченского полуострова 
  на Таманский с целью взаимодействия с войсками, наступающими на Краснодар. 2 
  сентября 1942 года части 46-й пд высадились в районах мыса Ахиллион, Кучугуры, 
  Синей Балки, а затем — на косу Чушка и Тузла. Вслед за 46-й пд переправились 
  части 3-й гсд (р). В дальнейшем на Северный Кавказ через пролив были переброшены 
  части 10, 18, 19 пд и 8-й кд (р) и 50-я пд немцев [25, с. 5].<br>
  <br>
  В архивных документах РО штаба 47-й Армии и СКФ наряду с материалами о групповой 
  разведке привлекают внимание сведения о работе одиночных негласных помощников 
  на занятой противником территории.<br>
  <br>
  Агентурная разведка была самым острым оружием в добывании информации о замыслах 
  и реальных действиях врага в Крыму и на побережье Северного Кавказа летом 1942 
  года. Наше командование в первую очередь интересовали намерения немцев по совершению 
  десантной операции для прорыва с Крымского плацдарма на Северный Кавказ и далее 
  — в Закавказье. Вот выдержка из плана разведки РО штаба СКФ на конец июня — 
  начало июля 1942 года:<br>
  <br>
  «Объекты и задачи:<br>
  <br>
  1. Определить район сосредоточения 46-й пд (нем.), 10-й и 16-й пд (р), отряда 
  Корнэ (р), расположение их штабов. Особое внимание — м. Хрони, Еникале, Керчь, 
  Камыш-Бурун, Сараймин, Тобечик, Феодосия, Влади-славовка.<br>
  <br>
  2. Следить за движением войск противника по железным и грунтовым дорогам — Владиславовка 
  — Керчь, Феодосия — Ленинское — Керчь, Дальние Камыши — Марфовка. Определить 
  состав обнаруженных колонн, род войск, глубину, интенсивность движения, количество 
  артиллерии и танков, районы сосредоточения.<br>
  <br>
  3. Следить за наличием переправочных средств в портах Керченского полуострова 
  и пунктах возможных переправ — Еникале, Керчь, Камыш-Бурун, Феодосия.<br>
  <br>
  4. Определить районы оборонительных работ противника на побережье Керченского 
  полуострова до рубежа восточнее Владиславовка, Феодосия; фланги и глубину обороны, 
  наличие противотанковых и противопехотных препятствий, боевой состав частей, 
  занимающих оборону.<br>
  <br>
  Состав и методы разведки:<br>
  <br>
  …Глубокая разведка<br>
  <br>
  1. Выбросить две группы парашютистов с рациями в районе Султановка и Ленинское.<br>
  <br>
  2. Восстановить связь с агентурными рациями № 21 и № 22 в Керчи (в одном случае 
  речь, видимо, идет об известной по архивным материалам КИКЗ радиоточке «Николаенко», 
  оставленной в Керчи раз-ведотделом штаба фронта в мае 1942 г. — Л. В.).<br>
  <br>
  3. Перевести на радиосвязь агентурные точки в Джанкое и Владиславовке.<br>
  <br>
  4. Организовать два постоянных наблюдательных пункта за дорогами Симферополь 
  — Карасубазар и Карасубазар — Керчь…<br>
  <br>
  …9. Организовать агентурную радиоточку в Мариуполе…<br>
  <br>
  …11. Забросить 3–5 агентов-маршрутников (с голубями) на северное побережье Керченского 
  полуострова.<br>
  <br>
  Спецразведка — Приступить к разработке итальянских шифров».<br>
  <br>
  Подписали: начальник 1-го отделения РО штаба СКФ майор Тоноян; начальник РО 
  бригадный комиссар Капалкин; военком РО — ст. батальонный комиссар Цицурин [26, 
  с. 35].<br>
  <br>
  Теперь посмотрим, как реализовались этот и подобные планы фронта в подчиненных 
  армиях, а именно в 47-й.<br>
  <br>
  Ответственным за организацию разведки на восточной оконечности Керченского полуострова 
  с 18 мая по 10 августа 1942 года был командующий 47-й Армией СКФ. В задачу фронта 
  в этот период входило — оборонять восточное побережье Азовского моря и Таманского 
  полуострова и не допустить форсирования противником Керченского пролива. Когда 
  47-я Армия была переброшена на прикрытие Новороссийска, оборону приняла Азовская 
  военная флотилия. Разведку она проводила силами и средствами КВМБ [27, с. 55].<br>
  <br>
  23 мая 1942 года Штарм-47 в приказании на разведку определил задачи. Для КВМБ 
  они формулировались так:<br>
  <br>
  «…а) Установить путем захвата пленных — какими частями противник занимает восточное 
  побережье Керченского полуострова от мыса Варзовка до Маяк-Казаульский и далее 
  — южное побережье до Феодосии…<br>
  <br>
  б) Обратить внимание на скопление войск и плавсредств в Камыш-Бурун, Эльтиген, 
  Феодосия [28].<br>
  <br>
  в) Установить тщательное наблюдение за местом возможной высадки десантов противника 
  на побережье Таманского полуострова от Тамань до Красноселовка».<br>
  <br>
  Темрюкской ВМБ предписывалось решать эту задачу на рубеже от Голубицкой до мыса 
  Литвин.<br>
  <br>
  Приказание подписал начальник штаба 47-й Армии генерал-майор Хрящев и начальник 
  РО подполковник Вахрамеев [29, с. 1–2].<br>
  <br>
  Представляют интерес планы оперативно-разведывательных мероприятий РО Штарма-47 
  и способы их реализации. Так, с 26 мая по 5 июня 1942 года предусматривалось:<br>
  <br>
  «1. Установить дислокацию штабов и войск 10-й и 19-й пд (рум.), 22-й тд и штабов 
  7-го и 42-го ак.<br>
  <br>
  2. Установить, какие части противника переброшены и продолжают перебрасываться 
  на запад в направлении Симферополя.<br>
  <br>
  3. Установить истинный состав оставшихся дивизий в районах Керчь и сев.-вост. 
  Тархун, Багерово, Султановка и далее на запад до Семь Колодезей.<br>
  <br>
  4. Установить, производит ли противник по восточному побережью Керченского полуострова 
  оборонительные работы и их характер.<br>
  <br>
  5. Установить характер грузов по дорогам от Владиславовка на Керчь и обратно».<br>
  <br>
  Для решения этих задач планировались следу-ющие мероприятия:<br>
  <br>
  «а) выброска маршрутных групп (2–3) по 2–3 человека;<br>
  <br>
  б) выброска второй очереди маршрутных групп для перепроверки (исполняли начальник 
  1-го отделения РО, начальник ОП № 1);<br>
  <br>
  в) выброска разведгрупп (2–3) по 2–3 человека из числа бойцов с приданным проводником 
  (исполнял начальник 1-го отделения РО);<br>
  <br>
  г) переброска отдельных осведомителей на «живой связи» для освещения действий 
  противника в Керчи, Марфовке, Семь Колодезей, Владиславовке.<br>
  <br>
  д) на Таманском полуострове — подготовка запасной сети осведомителей (исполняли 
  начальник 3-го отделения РО и начальник ОП № 1)».<br>
  <br>
  В приказаниях НШ 47-й Армии командирам подчиненных соединений предписывалось: 
  «Разведгруппы подобрать из наиболее дисциплинированных, смелых, физически крепких 
  разведчиков, могущих плавать в полном снаряжении от 50 до 100 метров» [30, с. 
  81–115].<br>
  <br>
  Нетрудно представить, что в демонстрационный поиск на верную смерть также посылались 
  лучшие бойцы.<br>
  <br>
  Дезинформируя противника, командование продолжало разведку занятой врагом территории 
  всеми доступными средствами и методами. Наиболее действенным и надежным средством 
  продолжала оставаться агентура. Поэтому автор снова делает акцент на роли агентурно-оперативных 
  мероприятий в планах военного командования Северо-Кавказского фронта и его составных 
  частей в боях за Крым и Керченский полуостров летом 1942 года.<br>
  <br>
  В плане мероприятий РО штаба 47-й Армии на июнь 1942 года числилось [31, с. 
  40]:<br>
  <br>
  «- создать радиофицированную резидентуру в Керчи с задачей освещения частей 
  противника, дислоцируемых в районах м. Хрони, Керчь, Еникале, и установления 
  инженерных парков;<br>
  <br>
  - создать радиофицированную резидентуру в Султановке с задачей освещения частей 
  противника в районе Султановка, Сараймин, Марфовка;<br>
  <br>
  - поставить перед существующей радиорезидентурой в Керчи (имеется в виду группа 
  «Тоня». — Л. В.) задачи освещения частей противника в Керчи, Камыш-Бурун, по 
  освещению системы ПВО, установлению инженерных парков и их оснащенности переправочными 
  средствами;<br>
  <br>
  - для освещения частей противника, дислоцирующихся в Ленинск, Ак-Монай, Феодосия 
  и Влади-славовка: подготовить и перебросить 5 маршрутных агентов;<br>
  <br>
  - для освещения движения войск и грузов противника по железной и грунтовой дороге 
  Владиславовка — Семь Колодезей — Керчь, Дальние Камыши — Марфовка организовать 
  отдельные осведомительные точки на живой связи в пунктах Ленинское, Марфовка, 
  Дальние Камыши, Керчь; для перепроверки подготовить маршрутных агентов;<br>
  <br>
  - для установления количества и типов самолетов на аэродромах Керчь, Багерово, 
  Марфовка, Харджи-Бие, Семь Колодезей, подобрать 2–3-х маршрутных агентов».<br>
  <br>
  Ответственными организаторами указанных меро-приятий назначены начальник 3-го 
  отделения РО 47-й Армии капитан Злобин и начальник ОП № 1 капитан Кваша. Эти 
  мероприятия соответствовали ранее упомянутому плану РО штаба СКФ. Сведений о 
  выполнении всех этих планов в архиве не обнаружены.<br>
  <br>
  Во второй половине июня — середине июля 1942 года агентурно-оперативные мероприятия 
  РО штаба 47-й Армии планируются совместно с разведотделом НКВД Крымской АССР. 
  Так, в плане на период с 19 июня по 15 июля 1942 года предусматривалось: переброска 
  19 июня с участием плавсредств КВМБ боевой разведгруппы (ответственные — работник 
  НКВД Романцов и НО-3 капитан Злобин); переброска с 20 июня морским путем (КВМБ 
  и ТВМБ) агентуры со спецзадачами (даты — 22, 30 июня; 3, 9, 10, 12, 15 июля 
  1942 г.). Сведения о выполнении этих мероприятий в архивах также не обнаружены 
  [32, с. 82]. Но известно, что именно в этот период в Керчь забрасывались негласные 
  сотрудники органов НКВД — Барков и Клюге. И, видимо, «ходки» в тыл совершались 
  ими неоднократно. Андрей Дмитриевич Барков уже в ноябре-декабре 1941 года в 
  тылу врага выполнял разведзадание Керченского горотдела НКВД. После отхода наших 
  войск вторично, в 1942 году, Барков вновь был оставлен в Керчи, но схвачен немцами 
  27 августа 1942 года. Вера Семеновна Клюге, 1919 г. р., летом и осенью 1942-го 
  была направлена в Керчь с заданием — собирать сведения о дислокации войск противника, 
  о движении эшелонов; заниматься выявлением действующих патриотов и тех, кто 
  служит оккупантам. Она была партизанской разведчицей, но находилась на связи 
  у сотрудников органов НКВД. Возможно, что ее неоднократная переброска и «ходки» 
  связника к ней выполнялись через пролив с участием военной разведки [33, с. 
  2–3].<br>
  <br>
  В июне-июле 1942 года задачи перед разведотделами СКФ усложняются. После 6 июня 
  1942 года в приказаниях НШ 47-й Армии подчиненным штабам стрелковых дивизий, 
  ТВМБ и КВМБ отмечается, что противник приступил к систематической выброске на 
  нашу территорию диверсионно-разведывательных групп и агентуры с целью дезорганизации 
  работы тыла и подготовки посадочных площадок для авиации [34]. Отсюда следовала 
  задача — наблюдение за местами возможной высадки авиадесанта противника на Таманском 
  полуострове. 77-й СД и 32-й гв. СД предписывалось взаимодействие с КВМБ, а 103-й 
  ОСБр — с ТВМБ.<br>
  <br>
  В июле 1942 года перед РО Штарм-47 командование СКФ поставило задачу по выявлению 
  признаков подготовки противника к ведению бактериологической войны (первичные 
  сведения получило РУ ГШ РККА и ориентировало фронтовые разведотделы).<br>
  <br>
  В этот и последующий период РО Штарм-47 сосредоточивает внимание на получении 
  данных об усилении авиационных сил противника на Керченском побережье: в Керчи, 
  Багерово и других пунктах. Так, в отчете о боевых действиях КВМБ с 20 мая по 
  6 сентября 1942 года зафиксирована ежедневная информация о посадке транспортных 
  и других немецких самолетов на Керченском, Аджимушкайском и Багеровском аэродромах 
  в количестве от 8 до 44 каждый раз. Всего в этот период (к 20 августа 1942 г.) 
  зафиксировано одновременно от 350 до 450 самолетов противника [35, с. 43]. Разведоперации 
  по захвату «языка» в июле 1942 года проводились с одновременным уничтожением 
  заготовленных немцами средств десантирования. Так, по заданию командования 47-й 
  Армии разведотряд КВМБ в составе двух ТК должен был высадить группу разведчиков 
  из 26 человек в районе Генуэзского мола. Их сопровождал СКА-016.<br>
  <br>
  В эту же ночь с такой задачей была проведена операция в районе Эльтигена с участием 
  25 разведчиков.<br>
  <br>
  Разведывательное обеспечение войсковых операций конечной целью ставило предоставление 
  командованию исчерпывающих данных о противнике. Способы их отработки достаточно 
  разнообразны. Например, агентурная информация перепроверялась авиаразведкой. 
  Так, в середине июня 1942 года начштаба 47-й Армии дал приказание НШ ВВС армии: 
  «По агентурным данным подтверждается подготовка десанта в районе Маяк-Еникале. 
  Плавсредства находятся в специально вырытых тоннелях на берегу. На аэродроме 
  Багерово в капонирах сосредоточено до 400 самолетов. В Казенном саду в Керчи 
  большое скопление автомашин. Требуется авиаразведкой уточнить все эти данные» 
  [36, с. 75].<br>
  <br>
  В связи с резким обострением обстановки в июле 1942 года командование армии 
  в соответствии с директивой СКФ организует и в ночь с 9 на 10 июля 1942 года 
  проводит операцию по заброске четырех разведотрядов от дивизий и КВМБ, в задачу 
  которых входило захват «языков» и установка наличия плавсредств противника в 
  районе Керчь — Еникале. В итоге отряды были рассеяны огнем врага, понесли потери. 
  Задача перепроверки сведений об оснащении войск противника десантными плавсредствами 
  и их уничтожение при обнаружении и на этот раз оказалась невыполненной.<br>
  <br>
  В этот же период, например, КВМБ захватом контрольных пленных предписывалось 
  установить нумерацию частей на восточном побережье Керченского полуострова от 
  Опасного до мыса Такыль, а также характер оборонительных работ [37, с. 35].<br>
  <br>
  Работа разведывательных маршрутных групп в интересующих командование районах 
  перепроверялась выброской второй очереди маршрутников. Это являлось характерным 
  для разведмероприятий в начале июня 1942 года.<br>
  <br>
  После сопоставления всех видов разведки штабу 47-й Армии была представлена итоговая 
  сводка № 5 за период с 20 по 30 июня 1942 года. В разделе IV «Возможность высадки 
  десанта» говорилось: «По агентурным данным противник готовит десант для высадки 
  на Таманский полуостров, с этой целью в районах Маяк, Жуковка, Опасное, Еникале 
  сосредоточены и тщательно замаскированы плавсредства. За последнее время противник 
  по железной дороге подвозит плавсредства в Керчь и Феодосию. 20.06.1942 г. в 
  Керчь прибыло 2 эшелона с понтонным имуществом. На плавсредствах, находящихся 
  на побережье Керченского полуострова, кроме привезенных за последние 15 дней 
  по железной дороге, противник может одновременно поднять до 8500 человек. Кроме 
  этого могут быть использованы плавсредства, находящиеся в Мариупольском порту, 
  где имеются понтоны грузоподъемностью до 50 тонн и лодки на 25–30 чел. каждая. 
  В Мариуполе находится до 1500 чел. морской пехоты, якобы предназначенной для 
  отправки на Керченский полуостров» [38, с. 29].<br>
  <br>
  Все сводки РО армий на первом месте содержали информацию от негласных зафронтовых 
  источников. Уже упоминавшаяся радиофицированная разведгруппа «Тоня», действовавшая 
  в Керчи, давала разнообразную информацию, охватывая обширную территорию от Еникале 
  до Багерово. Это иллюстрируют радиограммы, почти полностью приведенные в статьях 
  В. Абрамова и А. Рябикина [39].<br>
  <br>
  Из архивных сводок РО 47-й Армии следует, что в середине июля 1942 года глубинная 
  разведка стала получать информацию от новых источников, дополняющих «Тоню». 
  Впервые эти сведения отражены в итоговых сводках за 13–17 июля 1942 года. Так, 
  17 июля к 8.00 разведотдел Штарм-47 докладывал: «По агентурным данным: основные 
  силы противника из Крыма не выводились. Из Симферополя на Керчь отмечается большое 
  движение пехоты и артиллерии. Немцы якобы готовят наступление через Керченский 
  пролив. В направлении Керчи по ж/д дороге отправлено 1000 лодок марки 12М. От 
  Карасубазар в направлении Керчь последовала немецкая дивизия в ночь на 15.07 
  Из Симферополя на Карасубазар проследовали мотомехчасти, кавалерия, обозы… 14–15.07 
  от Салынь на Феодосию проследовало 280 автомашин и 900 подвод… Вывод: противник 
  перебрасывает части и плавсредства на Керченский полуостров и подготавливает 
  десантную операцию». Именно этих сведений в радиограммах «Тони» нет. У фронтового 
  разведотдела появились новые источники на узлах железных и шоссейных дорог в 
  районе Джанкоя, Судака, Салыня; во второй половине июля 1942 года они сообщали 
  о следовании на Керчь железнодорожных эшелонов с орудиями, разобранными самолетами, 
  сотен подвод, десятков грузовых машин [40, с. 29, 55–57, 62]. Но подчеркнем 
  еще раз: в конце мая — начале августа 1942 года здесь, на восточном побережье 
  Керчен-ского полуострова, контролируемого силами и средствами разведорганов 
  47-й Армии СКФ, единственным и надежным источником долгое время оставалась резидентура 
  «Тоня», возглавляемая разведчицей Евгенией Антоновной Дудник. О ней много написано 
  в 70–80-е годы. Но почти нет публикаций о ее наставниках в военной разведке. 
  Автор попытался заполнить этот пробел.<br>
  <br>
  Наиболее квалифицированные специалисты по агентурной (глубокой) разведке в этот 
  период находились в оперативном пункте № 1, прикомандированном еще в конце 1941 
  года к РО штаба 51-й Армии, а затем переподчиненным начальнику РО 47-й Армии, 
  оборонявшей Таманский полуостров. Личный состав ОП № 1 прибыл в Крым вместе 
  со штабом Приморской армии из-под Одессы после оставления города нашими войсками. 
  Вместо передислокации в Ростов-на-Дону в распоряжение штаба Южного фронта ОП 
  № 1 командованием ГРУ был оставлен в подчинении РО 51-й Армии для решения задач 
  в Крыму, угроза потери которого к этому времени воспринималась руководством 
  вполне реально.<br>
  <br>
  О личном составе ОП № 1 автор располагает информацией по воспоминаниям одного 
  из офицеров этого подразделения — Степана Федоровича Багнюка [41]. Использовались 
  также учетные карточки на некоторых его сослуживцев по ОП № 1, хранящиеся в 
  11-м отделе ЦАМО РФ (г. Подольск).<br>
  <br>
  Это были кадровые разведчики. С. Ф. Багнюк начал служить в августе 1938 года. 
  Был курсантом Тамбовского пехотного училища. После выпуска в январе 1940 года 
  включен в состав отряда дальних разведчиков под командованием полковника Мамсурова 
  [42], действовавших в полосе 9-й Армии на финском фронте. По окончании боевых 
  действий С. Ф. Багнюк служил командиром взвода пешей разведки 417-го сп 156-й 
  СД в Одесском военном округе. В декабре 1940 года был направлен в распоряжение 
  ГРУ РККА и прошел подготовку как разведчик-агентурист. Перед войной прибыл в 
  Кагульский оперпункт РО штаба ОдВо. Проводил агентурные мероприятия против Румынии, 
  союзницы гитлеровской Германии. С началом войны в июне 1941 года как помощник 
  начальника ОП работал по подбору и подготовке негласных сотрудников из местных 
  жителей для оставления их в тылу в случае отхода наших войск. В августе 1941 
  года Кагульский ОП слился с Измаильским ОП. С переброской Приморской армии в 
  Крым весь личный состав оперпункта (около 30 чел.) действовал по своему прямому 
  назначению. Начальником ОП в это время был капитан Андрей Гаврилович Кваша.<br>
  <br>
  В 1933 году он окончил два курса Харьковского института физкультуры. Здесь в 
  1941 году экстерном сдал экзамены за курс военного училища связи. Тогда же прошел 
  подготовку на курсах усовершенствования начсостава подразделений разведки. Военная 
  служба его началась в 1939 году, был командиром роты связи, адъютантом командира 
  корпуса. В феврале 1941 года назначен помощником начальника 35 оперпункта (Измаильского 
  ОП) ОдВо.<br>
  <br>
  Начальником радиостанции был лейтенант Алексей Андреевич Гаврилюк, призванный 
  в РККА в 1933 году. Прослужив два года радиотелеграфистом в радиоразведывательном 
  дивизионе, окончил курсы младших лейтенантов, с 1939 года стал начальником радиостанции 
  ОП штаба ОдВо.<br>
  <br>
  Помощником начальника ОП — шифровальщиком работал лейтенант Щеголев. Замполитом 
  был политрук Семен Гильдман. Среди сотрудников было несколько человек, аттестованных 
  на офицерское звание, которые ранее проявили себя как опытные разведчики-закордонники. 
  Заместителем начальника ОП был старший лейтенант Иван Смирнов.<br>
  <br>
  В Керчи ОП с октября 1941 года располагался в большом многоэтажном доме на Набережной 
  улице. Всех жильцов отселили. Личный состав обмундировали в пограничную форму. 
  В персонал входили люди из Керченской таможни — пограничники лейтенант Чайка 
  и мл. лейтенант Осыпа.<br>
  <br>
  Офицеры-оперативники главной задачей ставили создание в тылу оккупантов сети 
  надежных источников информации из числа местных жителей. Эти операции офицеры 
  ОП проводили заблаговременно, но в короткий срок, так как обстановка на фронте 
  в октябре 1941 года резко изменилась не в нашу пользу. Например, С. Ф. Багнюк, 
  используя свой особый мандат о полномочиях, подписанный ранее командующим Приморской 
  армии генералом Чибисовым, в Керченском военкомате по учетно-послужным карточкам 
  радистов отбирал подходящих людей, проводил беседы со многими из них. Всего 
  к ноябрю 1941 года в районе Керчи Багнюком было подготовлено и проинструктировано 
  не менее 10 местных жителей. Большая часть из них имела документы о непригодности 
  к призыву на военную службу. Были и женщины. Так, радисткой оставлена керчанка 
  Дудник Евгения Антоновна — «Тоня». Но сразу радиостанцией обеспечена не была 
  по причине отсутствия агентурных раций в оперативном пункте.<br>
  <br>
  Поэтому ей надлежало выполнять задание оперпункта — собирать данные о войсках 
  оккупантов и их передвижении в Керчи. Связь — по паролю через связника. Других 
  негласных сотрудников разведки она не знала и выйти на связь с ОП самостоятельно 
  не могла.<br>
  <br>
  После овладения нашими войсками Керченским полуостровом в начале января 1942 
  года начальник РО 51-й Армии подполковник Шилов приказал личному составу ОП 
  переправиться в Керчь и с отходом немецких войск в глубь полуострова отправлять 
  туда своих людей. ОП располагался в деревне Киет в районе Семь Колодезей. КП 
  51-й Армии располагался в селе Семисотка. Разведотдел Крымфронта в это время 
  находился в катакомбах возле поселения Ленин-ское. Отсюда офицеры ОП проводили 
  разведоперации в направлении Джанкоя и Перекопа, от Сольпрома, с Арабатской 
  стрелки отправляли людей на шлюпках в глубь территории, занятой противником.<br>
  <br>
  Во время отхода наших войск к Керчи и Еникале офицеры ОП участвовали в действиях 
  заградотряда, состоящего из батальона парашютистов-десантников и пограничников, 
  обеспечивали оборону переправы.<br>
  <br>
  В мае 1942 года, когда наши войска уже оставили Керчь, в Темрюке закончилась 
  подготовка разведчицы «Тони» и ее напарника «Сергея». Руководителем по специальности 
  радистки был Гаврилюк, по шифрработе Женю готовил офицер Щеголев.<br>
  <br>
  Лейтенант Гаврилюк 15 мая 1942 года получил тяжелое ранение в живот и выбыл 
  из строя. Ранее, 12 мая, был ранен капитан Кваша, и начальником оперпункта № 
  1 при разведотделе штаба 47-й Армии стал капитан Смирнов.<br>
  <br>
  В связи с болезнью начальника РО Штарм-47 подполковника И. М. Вахрамеева в мае-июне 
  1942 года указания офицерам ОП № 1 давал комиссар раз-ведотдела — И. Ф. Стеценко. 
  Последний в систему органов разведки пришел с должности комиссара полка только 
  в январе 1942 года. Член партии с 1932 года, служил в РККА с 1930-го, окончил 
  полковую школу, трехмесячные КУКС. Иван Фомич Стеценко проявил «склонность и 
  желание к агентурной работе, лично провел неоднократные операции по заброске 
  агентуры на лодках через Сиваш и Керченский пролив» [43, с. 34].<br>
  <br>
  В 1942 году перед руководимым им РО и ОП № 1 стояли особо сложные задачи — проведение 
  глубинной разведки через пролив. И здесь, видимо, сказалось отсутствие опыта 
  оперативной работы. В первую очередь это отразилось на условиях использования 
  резидентуры «Тоня».<br>
  <br>
  В своих воспоминаниях [44, с. 16, 65] Стеценко отмечает, что оставление этой 
  разведгруппы проходило скоротечно, без серьезной подготовки. Существенны два 
  момента: первое — планы заброски в июне-июле 1942 года агентурных групп на Керчен-ском 
  направлении, за которое отвечал РО 47-й Армии, не выполнялись. Иван Фомич указывает: 
  «Необходимо отметить, что оставленные нами два человека с рацией были единственными 
  разведчиками, которые тогда в очень тяжелой обстановке передавали нам очень 
  ценную развединформацию». И отсюда следует второе.<br>
  <br>
  К концу месяца регулярного радиообмена из Керчи на Тамань «Тоня» выходила на 
  связь 38 раз, утром, днем и вечером, по расписанию. За эти сеансы «Тоня» передала 
  27 радиограмм. Это очень интенсивная работа радиопередатчика, подверженная угрозе 
  пеленгации радиоконтрразведкой противника [45]. Тем не менее руководство РО 
  штаба 47-й Армии 22 июня 1942 года дает письменное предписание начальнику ОП 
  № 1 капитану Смирнову: «Во исполнение общих задач разведки 47-й Армии перед 
  Керченской радиоосведомительной точкой поставить следующие задачи…» и перечисляет 
  вопросы: о группировке противника в Керчи, инженерных сооружениях, оборонительных 
  возможностях на побережье. Далее предписание гласило: «Указать, чтобы для выполнения 
  задач использовали все возможности, — родственников, знакомых, другие связи. 
  Срок выполнения — 30.06.1942 г.». Радиограмма пошла 25 июня 1942 года. У «Тони» 
  оставалось только пять дней на их выполнение.<br>
  <br>
  В течение следующего месяца «Тоня» выходила на связь 30 раз и передала 29 радиограмм.<br>
  <br>
  Нами установлено, что группа «Тоня» перед отходом советских войск в Керчи оставалась 
  не одна [46]. Так, три радиограммы поступило от разведчика «Маильяна» — Сергея 
  Аркадьевича Аванесова, после чего связь прервалась и дальнейшая его судьба неизвестна. 
  С отходом наших войск в мае 1942 года в Керчи разведотделом фронта оставлен 
  Ефим Кириллович Танасиенко (он же Михаил Иванович Николаенко) (КИКЗ, архив. 
  — Оп. 3. — Д. 424).<br>
  <br>
  15 июля 1942 года под псевдонимом «Игнат» в Керчь направлен Витольд (Виктор) 
  Иосифович Малькевич, 1922 г. р. Военная разведчица «Тамара», Нина Ильинична 
  Чаплыгина, 1922 г. р., в середине июня 1942 года морским путем заброшена с Тамани 
  в Мариуполь. После трех сеансов радиосвязи она была арестована немцами.<br>
  <br>
  Характерно, что радистам удавалось провести всего по три сеанса связи.<br>
  <br>
  Каким же образом в течение почти 70-ти сеансов «Тоня» могла оставаться вне контроля 
  оккупантов?<br>
  <br>
  Женя Дудник была надежным источником для военной разведки. Она мастерски владела 
  способами передачи радиоинформации: несмотря на помехи, выход в эфир происходил 
  быстротечно, в считанные минуты. При минимальном расходе сухих батарей она ухитрилась 
  передать несколько тысяч цифровых групп. Поэтому радиоконтрразведка столь продолжительное 
  время не могла зафиксировать передатчик радиоточки.<br>
  <br>
  Связь с «Тоней» прервалась 7 августа 1942 года неожиданно, в ходе радиосеанса, 
  при котором она уведомляла о готовности принять самолет для установления связи 
  командования с «партизанами» Керчи.<br>
  <br>
  Очевидно (версия автора статьи), с июля 1942 года Женя Дудник и ее напарник 
  были втянуты немцами в радиоигру с нашими разведорганами для выявления замыслов 
  советского командования и дезинформации его относительно планов по подготовке 
  десанта на Тамань. Может быть, «Тоня» давала понять нашему радиоцентру о своей 
  работе под контролем. Но, вероятно, радисты не приняли или не поняли этого сигнала. 
  Разведотдел 47-й Армии или ЧГВ ЗКФ продолжал использовать открывшуюся вдруг 
  возможность посылки людей и техники партизанам, а к 7 августа 1942 года намеревался 
  направить самолет на занятую противником территорию, где его могли ждать каратели. 
  Дудник не выдержала такого испытания и разбила рацию, чтобы пресечь радиоигру.<br>
  <br>
  ОП был выведен из-под контроля РО штаба 47-й Армии в августе 1942 года и в дальнейшем 
  действовал в составе разведки Закавказского фронта. К декабрю 1942 года от этих 
  органов в Керчи действовали две осведомительно-радиофицированные точки, в Джанкое 
  — пять [47].<br>
  <br>
  Радиоигры с нашей разведкой немцы практиковали широко. Захватив в начале весны 
  1944 года военных разведчиц на территории Ленинского района Крыма, спецслужбы 
  оккупантов провели радиоигру «Тамара», отправив три исходящих радиограммы и 
  получив столько же входящих из нашего разведцентра. Радиоигра «Анна» позволила 
  противнику дать три радиограммы, но мероприятие сорвалось: партизаны освободили 
  из тюрьмы наших патриотов, посвященных в планы спецслужбы немцев по проведению 
  радиоигры [48, с. 71].<br>
  <br>
  В связи со спешной, массовой подготовкой и заброской разведгрупп учет их оказался 
  поставленным настолько плохо, что в военкоматах долгие годы таких патриотов-военнообязанных 
  считали дезертирами или пропавшими без вести [49, с. 3]. С мая 1943 года массированная 
  заброска разведгрупп, оснащенных связью, в тыл противника значительно расширила 
  фронт партизанской борьбы, ибо такие группы «обрастали» местными патриотами, 
  преобразовываясь в партизанские отряды.<br>
  <br>
  Со временем военное командование стало иметь возможность перепроверять разведданные 
  своих источников через партизанскую разведку. И лишь после того, как параллельные 
  инстанции подтверждали точность сведения, на военные объекты гитлеровцев обрушивались 
  бомбовые удары. Так, группа Н. Эльяша дала целеуказание нашей авиации относительно 
  аэродрома в поселке Сарабуз. Эти данные продублировали материал радиограммы 
  из группы военных разведчиков «Верный» (Ф. Т. Илюхина). Известно, что они так 
  же, как и группа М. Я. Снесова, следили за передвижением войск и за гарнизонами 
  противника в Крыму, за отступлением из-под Никополя, за переправами через Днепр 
  в районе Херсона.<br>
  <br>
  На базе упомянутого Бахчисарайского партизанского отряда в июне 1943 года действовала 
  радиофицированная группа Ф. Ф. Волончука, заброшенная для разведки района Симферополя, 
  Севастополя и Ялты [50, с. 289]<br>
  <br>
  Здесь требуется внести необходимое уточнение в историю вопроса.<br>
  <br>
  Из редакционной статьи в томе № 6 Крымской Книги Памяти можно понять, что обнаружившие 
  в июне 1942 года тщательно замаскированное местоположение восьмисотмиллиметровой 
  пушки дивизиона «Дора» под Севастополем разведчик Н. Кожухарь и радист М. Дмитриев 
  являлись партизанами Бахчисарайского отряда. Но из книги М. Македонского «Пламя 
  над Крымом» видно, что эти люди относились к военной разведке [51, с. 91].<br>
  <br>
  Другой партизанский командир — Ф. И. Федоренко — в своих воспоминаниях пишет, 
  что в январе 1942-го основы для обмена информацией — радиосвязи с Большой землей 
  — ЦШПДК все еще не имел. Во II районе (руководитель — И. И. Генов) командование 
  все же получило доступ к радиостанции находившихся здесь армейских разведчиков. 
  Командующий Мокроусов устремился во II район с расчетом иметь связь с фронтом. 
  20 февраля 1942 года Генов по этому радио информирует штаб Крымфронта о скоплении 
  артиллерии и живой силы румын в районе горы Средней. Советская авиация нанесла 
  бомбовый удар. Ф. И. Федоренко приводит такую удручающую информацию: еще до 
  оккупации Крыма были выделены и оставлены в тылу врага десятки коммунистов, 
  но оказалось, что Центральный штаб не может связаться с ними: не известны ни 
  ЯК, ни пароли, ни клички. Разведработа была провалена [52, с. 41].<br>
  <br>
  Начальником разведки и службы безопасности ЦШПДК был И. Н. Казаков, а после 
  него — с весны 1942 года И. Х. Давыдкин. Но были ли они виноваты?<br>
  <br>
  Из доклада С. М. Буденного в «верха» от 13 июня 1942 года следует, что «партизанский 
  отряд Крыма в разведке проявил себя активно, хорошо», но действиями партизан 
  и их снабжением занимался только начальник РО фронта бригадный комиссар Капалкин. 
  Последний не смог решить вопрос обеспечения устойчивой связи для получения оперативной 
  информации через командиров отрядов. Как видим, опять все упиралось в радиосвязь.<br>
  <br>
  В сентябре 1942 года отряд Федоренко все еще не имел радиосвязи с Большой Землей, 
  и для сообщения о положении партизан была использована действующая в отряде 
  рация спецгруппы НКВД под командованием Киселева, имевшего прямую связь с Москвой 
  [53, с. 104].<br>
  <br>
  В Судакском районе в начале 1942 года успешно действовал отряд партизан из десантников-военнослужащих. 
  В документах и воспоминаниях отмечается, что именно этот отряд вел большую разведывательную 
  работу, что подкреплялось наличием устойчивой радиосвязи со штабом ЧФ и Кавказского 
  фронта [54, с. 451]. Эта возможность объясняется тем, что в составе десанта 
  5 января 1942 года были два отряда разведчиков и контрразведчиков Черноморского 
  флота с рациями.<br>
  <br>
  Очевиден факт невозможности использования движения сопротивления в разведывательных 
  целях без соответствующего технического оснащения зафронтовой стороны.<br>
  <br>
  В самом деле, захватить рацию у противника не составило бы труда для любого 
  партизанского отряда. Но установить двустороннюю связь таким путем было невозможно. 
  Во-первых, необходимо было с армей-ским корреспондентом обусловить позывные, 
  рабочие и запасные частоты, расписание сеансов. А главное — оба корреспондента 
  должны были располагать документами скрытой связи (СУВ): кодами, шифрами, обусловленными 
  сигналами работы под контролем противника (на случай захвата радистов вражеской 
  контрразведкой), записью почерка радиста в эфире. Все это могло быть подготовлено 
  лишь при централизованном планировании, но никак не стихийно: документы СУВ 
  оберегались как государственная тайна.<br>
  <br>
  Поэтому для командования Северо-Кавказского направления реальным и приемлемым 
  представлялось создание из военнослужащих-партизан специального разведывательного 
  соединения в Крыму, оснащенного средствами радиосвязи. Вариант, предлагаемый 
  В. С. Булатовым и его штабом (обеспечение партизанских групп армейскими средствами 
  связи), для С. М. Буденного оказался неприемлемым.<br>
  <br>
  Позднее Крымский ШПД стал иметь свою радиосеть. Но разведсводки для армии и 
  флота если и поступали от командиров отрядов, то несвоевременно, ибо материал 
  поступал сначала в ЦШПД Крыма (В. С. Булатову) и только потом попадал в Военный 
  Совет фронта или армии, при этом терялось 2–3 дня. Отсутствие радиосвязи в отрядах 
  тормозило работу.<br>
  <br>
  К ноябрю 1943 года положение изменилось коренным образом. Но все же задержка 
  информации составляла не менее двух дней. Например, шифровки, отправленные Булатовым, 
  как начальником КШПД, о событиях 7 января 1944 года, поступили в войска только 
  9 января. В январе ежедневно Булатовым отправлялись шифровки начальнику ЦШПД 
  генерал-лейтенанту Пономаренко, командующему Отдельной Приморской Армией генералу 
  армии И. Е. Петрову, командующему ЧФ вице-адмиралу Владимирскому и командующему 
  51-й Армией генерал-лейтенанту Я. Г. Крейзеру [55, с. 7].<br>
  <br>
  В 1944 году разведка стала чрезвычайно важным направлением в деятельности партизан 
  и подпольщиков: она строилась на основе конкретных заданий, получаемых от командования 
  4-го Украинского фронта и Отдельной Приморской Армии. Обеспечивалась прямая 
  двусторонняя связь.<br>
  <br>
  В тылу противника в это время действовали уже девять радиостанций, в том числе 
  две в Симферополе. Накануне нашего наступления командование получило 4629 радиограмм 
  с разведданными о вражеской обороне в Крыму. Некоторые подпольные группы были 
  непосредственно связаны со штабами Красной Армии (Крымский драматический театр 
  — группа «Сокол», 10 человек).<br>
  <br>
  В немецком документе от 11 июня 1944 года указывалось: «Партизанские районы 
  являлись базами для подразделений разведки спецгрупп Красной Армии в качестве 
  мест пребывания и отправки, со своей сетью связи» [56, с. 275].<br>
  <br>
  Рации были не единственным способом связи. И это понятно. В 1942–1944 годах 
  применялся также воздушный способ заброски военных разведчиков и связников в 
  зоны действий партизанских отрядов Крыма. В этот период на территории Белогорского 
  (Карасубазарского) района, где действовали партизаны II района, находились основные 
  посадочные площадки для приема самолетов с Большой Земли.<br>
  <br>
  Но разведотделы трех армий Крымфронта проводили операции по заброске разведчиков 
  с использованием только необорудованных «пятачков» в глухой местности. Об этих 
  операциях имеется запись рассказа бывшего летчика самолета ПО-2 — Степана Степановича 
  Нетруненко. Он с января 1942 года служил в 764-м легкобомбардировочном полку 
  ночных полетов на ПО-2. Полк действовал в интересах 44-й Армии Крымфронта. Такой 
  же полк — 763-й — был придан 51-й Армии. Базировался 764-й ЛБНАП в районе деревни 
  Харджи-Бие (от дер. Марфовка под Керчью — в 7 км на юго-запад). ПО-2 (биплан, 
  бывший У-2, или «кукурузник») собрался из дерева и перкали, был очень надежен 
  в управлении. Летчики — это асы из числа инструкторов 25-й Невинномысской (на 
  Кубани) авиашколы по переучиванию пилотов ГВФ. Из полка были отобраны лучшие 
  проверенные летчики для участия в обеспечении разведывательных операций. Для 
  заброски разведчиков и чекистов командование использовало и другой тип самолета 
  — С-2 — с посадкой на оккупированной территории в ночное время без подсветки. 
  Как вспоминает С. С. Нетруненко, людей доставляли через линию фронта в нужную 
  точку (бывали среди них и молодые женщины), а потом в обусловленный день и час 
  сюда же прилетал уже знакомый с данным местом летчик и забирал разведчиков после 
  выполнения задания. (Известно, что Малькевич — «Игнат» трижды воздушным путем 
  доставлялся под Керчь и возвращался в разведцентр).<br>
  <br>
  Кроме риска удариться при посадке или напороться на препятствие при коротком 
  разбеге в момент взлета летчики рисковали попасть в «западню» при заходе на 
  ложные сигнальные огни на земле: немецкие контр-разведывательные органы, выбив 
  показания из за-хваченного нашего разведчика, нередко имитиро-вали посадочную 
  площадку на каком-нибудь забо-лоченном лугу, зажигая обусловленные костры. Неопытный 
  летчик мог бы среагировать на ложный сигнал и «влететь» в болото [57].<br>
  <br>
  Главной задачей разведки считалось получение данных, представляющих интерес 
  для действующей армии (подчеркнуто мною. — Л. В.). «Неоценимую пользу принесла 
  нам систематическая информация Крымских партизан», — отмечал командующий артиллерией 
  2-й гвардейской армии генерал И. С. Стрельбицкий [58, с. 123]. По подсчетам 
  Абвера, более 80% разведданных, полученных советским командованием с весны 1943 
  по весну 1944 года, поступило от партизан.<br>
  <br>
  И здесь необходимо подчеркнуть следующее.<br>
  <br>
  Партизанское движение в конце 1941 года и в первой половине 1942-го развивалось 
  по трем основным линиям:<br>
  <br>
  - действия созданных райкомами при участии органов НКВД накануне и в первые 
  дни оккупации небольших партизанских отрядов с присоединением к ним групп под 
  командованием колхозных активистов, секретарей сельсоветов, интеллигенции;<br>
  <br>
  - деятельность воинов Красной Армии, попавших в окружение, согласовывавших свою 
  работу с местными организациями сопротивления;<br>
  <br>
  - деятельность разведывательно-диверсионных и боевых групп, засылаемых с Большой 
  Земли.<br>
  <br>
  Отчеты и информация поступали в этот период в местные подпольные парторганы 
  и в учреждения, которые направляли патриотов во вражеский тыл [59, с. 21]. На 
  этом этапе просматривается их разобщенность.<br>
  <br>
  Военные советы фронтов были заинтересованы прежде всего в помощи отрядов, находившихся 
  в зоне их интересов и деятельности. Органы же НКВД ориентировали подчиненные 
  им отряды на проведение разведки сил и намерений противника. Партизанские отряды, 
  которыми руководили партийные органы, как правило, не имели двусторонней связи 
  с советским тылом. Поэтому сосредоточивали внимание на политических задачах.<br>
  <br>
  До середины 1942 года наблюдались параллелизм и дублирование в руководстве партизанскими 
  отрядами, который решал свою задачу: парторганы, органы НКВД, политуправления 
  фронтов и политотделы армий. Так, партизанские отряды 1-го и 2-го районов в 
  Крыму получали оперативные задания от командования Крымского фронта (затем — 
  СКФ); отряды 3-го и 4-го районов оперативно подчинялись Приморской армии. Так 
  не могло продолжаться.<br>
  <br>
  К июню 1942 года при армиях были организованы оперативные группы (ОГ) ШПД. Они 
  подчинялись ЦШПД, а в оперативном отношении — Военному совету соответствующих 
  фронтов. ОГ подготавливали и перебрасывали в тыл противника партизанские формирования 
  и диверсионные группы с целью содействия войскам на данном участке фронта; совместно 
  с партийными и советскими органами, армейскими политотделами организовывали 
  новые партизанские отряды в прифронтовой полосе; обеспечивали их оружием, снаряжением 
  и обмундированием; готовили и направляли в тыл своих разведчиков и связников; 
  передавали в военные советы полученную развединформацию с последующим уведомлением 
  соответствующего штаба партизанского движения; собирали документы германского 
  командования.<br>
  <br>
  Украинский штаб партизанского движения на 25 августа 1942 года имел радиосвязь 
  только с 36 отрядами (из 778). В остальных случаях действовали связники-курьеры. 
  Сведения зачастую теряли оперативную ценность [60, с. 81–88]. Как видно, это 
  было характерным для всей страны.<br>
  <br>
  К весне 1943 года в Саратовской школе радистов было подготовлено около 800 операторов 
  для работы в партизанских отрядах.<br>
  <br>
  То есть реальные условия для эффективного использования партизанского движения 
  в разведывательных целях сложились лишь к концу 1942 года.<br>
  <br>
  В Крыму же этот процесс затянулся в силу определенной линии на обретение необоснованной 
  (на взгляд автора) полной самостоятельности Штаба партизанского движения во 
  главе с В. С. Булатовым. Даже в августе 1943 года при постановке задач партизанским 
  формированиям нелегальный обком (В. С. Булатов) и подпольный Крымский обком 
  (Ямпольский) на первое место выдвигали усиление политической работы среди населения, 
  затем — диверсионной деятельности, и на послед-нем месте —разведывательных мероприятий.<br>
  <br>
  Когда же речь заходит о действиях отрядов и групп, руководимых кадровыми военными, 
  можно уловить разницу в расстановке акцентов относительно задач разведывательной 
  работы.<br>
  <br>
  Так, о направленности действий упомянутой выше группы майора Ш. Б. Чернянского 
  на основании архивных материалов, хранимых в Крыму и Москве, можно понять, что 
  заброска негласных помощников на территорию Северного Крыма осуществлялась им 
  в первую очередь с целью сбора разведданных, а уж затем — агитационно-пропагандистской 
  и, наконец, диверсионной работы [61, с. 103].<br>
  <br>
  Активизация глубокой разведки начиная с осени 1943 года на территории Северо-Западного 
  Крыма и в его восточной части (Керченский полуостров) была связана напрямую 
  с подготовкой массированного наступления наших войск с целью уничтожения немецко-румынской 
  группировки войск в Крыму и освобождения полуострова.<br>
  <br>
  Именно в этот период разведотдел Северо-Кавказского фронта проводил активные 
  мероприятия по за-броске разведгрупп и созданию резидентур, оснащенных средствами 
  радиосвязи, на основных узлах железных и шоссейных дорог, в местах группировки 
  войск противника, оборонявшего Керченский полуостров. К этому времени система 
  агентурной разведки армии и флота была восстановлена (напомним, что до весны 
  1943 г. проведение агентурных операций за линией фронта в звене армия—фронт 
  осуществлялись по линии органов НКГБ и военной контрразведки).<br>
  <br>
  Усилиями разведки СКФ в сентябре 1943 года были получены данные о появлении 
  в Крыму семи дивизий противника (4 — немецких и 3 — румынских), переброшенных 
  с Тамани; о прибытии на аэродромы Крыма значительного количества бомбардировочной 
  авиации; о переброске войск в Восточный Крым морем, по дорогам и железнодорожным 
  транспортом; о насыщенности побережья окопами, ДЗОТами и другими инженерными 
  средствами. Здесь было сосредоточено до 85 тыс. сухопутных войск при 60–70 танках 
  и 50 минометных батареях [62].<br>
  <br>
  С целью борьбы с партизанами и советскими разведчиками немецкое командование 
  к августу 1943 года эвакуировало большинство местных жителей из Керчи, Феодосии, 
  сел Керченского полуострова и побережья в северные районы Крыма.<br>
  <br>
  В этих условиях появление среди оставшегося населения любого нового человека 
  сразу же привлекало внимание немецко-румынских контрразведывательных органов. 
  Поэтому тактика руководителей разведорганов СКФ сводилась к подбору, подготовке 
  и заброске в нужный район разведчиков из числа местных граждан. Предпочтение 
  отдавалось женской агентуре. 2 октября 1943 года РО СКФ в районе села Кашик 
  Ленинского района была высажена группа Алиме Абденановой (1924 г. р.), крымской 
  татарки, ранее проживавшей в этом районе, имевшей близких родственников в деревне 
  Кашик. В подчинении резидента Абденановой (псевдоним — «София», «Аня») была 
  радистка — «Стася», «Гордая». Обучение обе проходили в течение месяца. Резидентура 
  включала 14 человек из местных жителей татарской национальности. От нее поступило 
  в РО СКФ более 80 радиограмм о численности и дислокации вражеских войск на Керченском 
  полуострове, их передвижении по железным и шоссейным дорогам [63, с. 8–20]. 
  Судя по донесениям немецких спецслужб об арестах советских разведчиков в предшествующий 
  период (1942–1943 гг.), наши армейские и фронтовые РО практиковали массовую 
  заброску именно девушек-разведчиц в районы Восточного Крыма. Способ преодоления 
  линии фронта — самолетом с парашютным прыжком. Это однообразие в тактике советских 
  военных разведорганов помогало немецко-румынским спецслужбам [64].<br>
  <br>
  В этом отношении, вероятно, более гибкой была тактика специальных разведподразделений, 
  действовавших в интересах 4-го Украинского фронта.<br>
  <br>
  Например, уже упоминавшаяся оперативная группа УШПД при ВС 51-й Армии в ноябре 
  1943 года перебросила в Крым ядро разведгруппы в составе: командир С. А. Гусев, 
  комиссар В. П. Зоркин, начальник штаба И. В. Шибаев. Способ заброски — парусной 
  лодкой через Каркинитский залив на территорию Ак-Шеихского района (прежнее название 
  Раздольненского района. — Л. В.). Группа пополнилась за счет местных патриотов. 
  Один из ее опорных пунктов был создан на Садырском (Славянском) молокозаводе. 
  Руководил этой небольшой группой Александр Ким. Кроме того, в нее входили брат 
  Александра — Антон, из соседнего села Бакал — Григорий Руденко, работники завода 
  А. П. Маслова и В. И. Озерова. Подобные группы были созданы и в других населенных 
  пунктах. Антон Ким был командиром диверсионной группы. Он дважды переправлялся 
  на лодке через залив, отвозил разведданные начальнику спецгруппы майору Ш. М. 
  Чернявскому, получал инструктаж, оружие, боеприпасы и обратным рейсом доставлял 
  их в Ак-Шеихский район.<br>
  <br>
  О результатах боевой деятельности разведчиков и их помощников, объединенных 
  под руководством майора Чернявского (по другим данным — Чернянского), каких-либо 
  обобщенных данных в нашем распоряжении не имеется. Известно, что этими отрядами 
  было уничтожено от 640 до 1426 оккупантов [65, с. 104]. Другие сведения достаточно 
  противоречивы и куцы.<br>
  <br>
  В отличие от тактики широкого охвата граждан путем вовлечения их в движение 
  сопротивления (что было характерным для первых двух лет борьбы), спецгруппы 
  НКВД в 1943–1944 годах разведывательную работу и внедрение в фашистские спецорганы 
  осуществляли малыми силами, с привлечением вновь создаваемых формирований из 
  проверенных представителей местного населения. Объясняется это следующим: наличием 
  в прежнем подполье немецкой агентуры и случайно вовлеченных в борьбу людей; 
  многие из подпольщиков оставались под контролем спецслужб оккупантов как «приманка».<br>
  <br>
  Подводя итог, мы можем констатировать, что основными и наиболее характерными 
  особенностями разведывательного обеспечения боевых действий в Крыму, в том числе 
  на Керченском полуострове, в период оборонительных операций здесь в 1941–1942 
  годах и освобождения юга нашей страны в 1943–1944-м являются:<br>
  <br>
  - участие в разведоперациях армий и фронтов (Кавказского, Крымского, Северо-Кавказского) 
  квалифицированных сотрудников специальных оперативных пунктов (ОП), в совершенстве 
  владевших способами агентурной разведки и глубокого проникновения в секреты 
  и планы командования оккупационных группировок в Крыму;<br>
  <br>
  - непосредственная отработка разведотделами СКФ, 47-й Армии и др. совместно 
  с аппаратами органов госбезопасности конкретных элементов дезинформационных 
  планов и мероприятий в интересах снижения активности немецко-румынских войск 
  на территории Восточного и Западного Крыма и облегчения таким путем положения 
  защитников Севастополя весной и летом 1942 года;<br>
  <br>
  - привлечение оперативных возможностей органов госбезопасности в разведывательных 
  целях по планам военного командования в звене армия—фронт;<br>
  <br>
  - трудности, подстерегавшие руководителей спецслужб наших войск при реализации 
  планов агентурного проникновения в немецко-румынские группировки на полуострове, 
  и смертельный риск при выполнении этих планов негласными сотрудниками разведслужб 
  армии и органов НКВД, что являлось следствием высокого профессионализма контрразведывательных 
  подразделений противника по всему Крыму;<br>
  <br>
  - использование партизанских баз и отдельных групп коммунистического подполья 
  в определенные периоды для действий разведывательных резидентур и формирований 
  особого назначения на основе тактики «отсечения» новых источников информации, 
  приобретавшихся разведслужбами в 1943–1944 годах, от «старого» подполья, что 
  обеспечивало их живучесть и позволяло избегать проникновения внутрь организованных 
  групп агентуры противника.<br>
