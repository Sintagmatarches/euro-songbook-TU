	<strong>Олег 
  Романько, кандидат исторических наук, доцент кафедры украиноведения Крымского 
  государственного медицинского университета им. С.И. Георгиевского (Симферополь)</strong><br>
  <br>
  <span class="header"><strong>Забытый урок Ялтинской конференции <br>
  <br>
  К ВОПРОСУ О НАСИЛЬСТВЕННОЙ РЕПАТРИАЦИИ ЧЛЕНОВ «ВОСТОЧНЫХ» ДОБРОВОЛЬЧЕСКИХ ФОРМИРОВАНИЙ 
  ЗАПАДНЫМИ СОЮЗНИКАМИ В СССР (1945—1947) </strong><br>
  <br></span>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=89&par=74&l=&art=342" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, №9, 
  2005.</em></a><br>
  
  <br>
   <br>
  <img src="a-zaburok1.jpg" width="200" height="190"><br>
 <br>
  В феврале этого года исполнилось ровно 60 лет со дня начала Ялтинской (Крымской) 
  конференции глав трех держав антигитлеровской коалиции — СССР, Великобритании 
  и США. Можно по-разному относиться к этому событию: считать его важным, поворотным 
  пунктом истории, почти на полвека определившим устройство послевоенного мира 
  или рассматривать как сговор более сильных государств, поделивших сферы влияния 
  в Европе за счет слабых. У каждого может быть своя точка зрения. Однако в любом 
  случае необходимо признать, что с 4 по 11 февраля 1945 года в Ливадийском дворце 
  происходило действительно очень важное событие, результаты и уроки которого 
  не потеряли своей актуальности ив наше время.<br>
  <br>
  История Ялтинской конференции изучена в отечественной и зарубежной историографии 
  довольно хорошо. Каждый интересующийся этой темой может узнать, что на конференции 
  были приняты решения о послевоенной политике трех великих держав, направленной 
  на установление прочного мира и обеспечение системы международной безопасности, 
  о создании ООН, о судьбе Германии после ее капитуляции и т. д. Кроме того, в 
  ходе этой конференции рассматривались и такие специфические вопросы, как, например, 
  польский и югославский (что, кстати, и является причиной высказываний об этой 
  конференции как о сговоре со стороны определенных политических кругов) [1].<br>
  <br>
  Однако в большинстве серьезных исследований по истории Ялтинской конференции 
  только за редким исключением можно встретить упоминание еще об одном принятом 
  в ее ходе решении [2]. На наш взгляд, это решение было не менее важным, чем предыдущие, 
  а по своей судьбоносности даже превзошло многие из них. В юридической плоскости 
  оно было закреплено в серии договоров глав союзных держав, а на практике обернулось 
  трагедией для миллионов людей. Речь идет о так называемой репатриации советских 
  граждан, волею судеб оказавшихся на территории оккупированной немцами Европы.<br>
  <br>
  Как известно, в ходе Второй мировой войны перемещались не только фронты и армии, 
  но и массы населения. Поэтому одним из вопросов, который предстояло решить И. 
  Сталину, Ф. Рузвельту и У. Черчиллю после окончания войны, была судьба этих 
  перемещенных лиц. Заинтересованность в скорейшем разрешении этого вопроса так 
  или иначе проявили все лидеры «большой тройки». Однако наибольшую актуальность 
  он приобрел для СССР, за пределами которого, в силу ряда причин, оказались миллионы 
  его граждан. В целом их можно поделить на три категории, каждая из которых могла 
  по-разному отнестись к репатриации. Но сначала немного истории.<br>
  <br>
  Одной из составляющих нацистской оккупационной политики на территории СССР был 
  массовый угон его населения на территорию Третьего рейха с целью дальнейшего 
  использования в качестве дешевой рабочей силы. Эти «остарбайтеры» составили 
  первую, самую многочисленную категорию будущих репатриантов. Другой категорией 
  советских граждан, оказавшихся за пределами своей Родины, были военнопленные. 
  И, наконец, была еще одна категория лиц, покинувших территорию СССР. Они, в 
  отличие от двух предыдущих, сделали это добровольно, вместе с отступающими немецкими 
  войсками. В число последних входили и бойцы так называемых «восточных» добровольческих 
  формирований — частей и соединений, созданных немцами из числа советских граждан. 
  В результате к маю 1945 года на территории Германии и оккупированных ею государств 
  оказалось около 7 млн перемещенных из СССР лиц всех указанных категорий. И если 
  подавляющее большинство «остарбайтеров» и военнопленных ничего не имели против 
  возвращения на Родину, то добровольцы и прочие коллаборационисты всеми силами 
  противились этому [3].<br>
  <br>
  Как казалось Рузвельту и Черчиллю, суть проблемы была проста. На территориях, 
  освобождаемых их армиями, имелось некоторое количество перемещенных лиц — граждан 
  союзной державы,которыхнеобходимо было отправить на Родину. Согласно подписанным 
  10 и 11 февраля 1945 года документам, все договаривающиеся стороны обязались 
  делать это как можно быстрее и без проволочек. Однако третий лидер «большой 
  тройки» — И. Сталин — понимал, что не все, оказавшиеся за пределами СССР, захотят 
  в него вернуться. Поэтому он настоял, чтобы лидеры западных союзников дали свое 
  согласие на «безусловную и всеобщую репатриацию всех находящихся в их оккупационной 
  зоне советских граждан» по состоянию границ на 1 сентября 1939 года. Это соглашение 
  подлежало выполнению без учета индивидуальных пожеланий. При необходимости допускалось 
  применение силы. И в первую очередь это касалось лиц, взятых в плен «в немецкой 
  военной форме», то есть бойцов «восточных» добровольческих формирований [4]. 
  Несколько позже, 13 марта и 26 июня 1945 года, соглашения, аналогичные ялтинским, 
  были заключены между СССР, Бельгией и Францией.<br>
  <br>
  Как отмечалось, с репатриацией подавляющего большинства «остарбайтеров» и советских 
  военнопленных не должно было возникнуть каких-либо проблем. Так оно, за редким 
  исключением, и происходило. Что же касается «беженцев от коммунизма» и бойцов 
  «восточных» добровольческих формирований, то их передачи советским репатриационным 
  комиссиям породили столько проблем и недоразумений, что эта тема стала надолго 
  закрытой для объективного исследования. Кроме того, за послевоенный период она 
  обросла целым клубком противоречий (в основном, политического, юридического 
  и морального характера), которые только искажали ее и так немногочисленные факты. 
  В связи с этимнастоящая публикация не является претензией на истину в последней 
  инстанции. Скорее, это только попытка приблизиться к пониманию вопроса, указанного 
  в ее заголовке. Иначе и быть не может. Даже в наше время, через 60 лет после 
  этих событий, многие материалы по насильственной репатриации в архивах Великобритании 
  и США являются недоступными в силу действующих там законов о хранении подобной 
  документации. Что же тогда говорить об отечественных архивах! Поэтому все сведения, 
  приведенные в статье, взяты из открытых источников и литературы, опубликованной 
  как на Западе, так и в странах СНГ.<br>
  <br>
  Одной из особенностей немецкой оккупационной и национальной политики в войне 
  против Советского Союза было активное привлечение его граждан к сотрудничеству. 
  Оно принимало различные формы, наиболее активной из которых являлась служба 
  в так называемых «восточных» добровольческих формированиях вермахта, войск СС 
  и полиции. Приходится признать, что эта политика имела определенный успех, так 
  как за период с 1941 по 1945 год в подобных формированиях прошли службу от 1,3 
  до 1,5 млн человек [5].<br>
  <br>
  Несмотря на такую цифру «предателей», их численность в действительности не так 
  ужвелика, если вспомнить историю СССР после октябрьского переворота. Ведь не 
  секрет, что перед началом войны в Советском Союзе было огромное количество недовольных 
  существующим режимом, настроения которых не мог не использовать осмотрительный 
  враг. А если прибавить еще и социальное недовольство, и нерешенный национальный 
  вопрос, то ситуация приобретала просто угрожающие размеры.<br>
  <br>
  Однако было бы ошибочным полагать, что в добровольческие формирования шли исключительно 
  идейные противники советской власти. В них было много и просто обманутых, и 
  военнопленных, доведенных до отчаяния нечеловеческими условиями в немецких концлагерях, 
  и просто тех, которые хотели выжить в условиях войны. Тем не менее, всех их 
  советская власть ставила на одну доску. Все они были «предателями», так как 
  воевали в рядах врага и заслуживали самого сурового наказания. Это показали 
  уже первые процессы над «предателями и изменниками Родины», которые начали организовываться 
  по мере того, как Красная Армия освобождала те или иные территории. Ярким примером 
  здесь может служить Краснодарский процесс (1943). Обычно приговор был стандартным 
  — смертная казнь через повешение [6]. Таким образом, часть добровольцев попала 
  в руки советских репрессивных органов еще до вступления Красной Армии на территорию 
  Европы. Еще одной части удалось отступить вместе с немецкими войсками (например, 
  «знаменитая» Бригада Каминского и Казачий стан атамана Доманова). Как правило, 
  вместе с ними уходили члены их семей и просто беженцы, не хотевшие возвращаться 
  под советскую власть. И, наконец, третья часть «восточных» добровольцев была 
  перемещена самими немцами с Восточного на Западный фронт в середине 1943 года. 
  Последние оказались разбросанными от Норвегии до Италии и от Польши до Франции 
  [7].<br>
  <br>
  Все вышесказанное касалось только советских граждан. Однако в число «восточных» 
  добровольческих формирований входили также части и соединения, созданные немцами 
  из представителей белой эмиграции, которые рассматривали войну Германии против 
  СССР как продолжение гражданской. К таким, например, относятся Русский охранный 
  корпус, составленный из чинов белой Добровольческой армии в Югославии, и 1-я 
  Русская национальная армия [8]. Назвать этих людей советскими гражданами можно 
  было только с очень большой натяжкой, тем не менее, как показали события, советская 
  власть также имела на них свои виды.<br>
  <br>
  В результате к маю 1945 года эта категория перемещенных лиц насчитывала более 
  2 млн человек (в их число входили также гражданские беженцы). Основная их масса 
  была сконцентрирована на территории Германии и Австрии, куда они отступали вместе 
  с немецкими войсками из всех уголков Европы. Кроме того, небольшая их часть 
  оставалась во Франции, Италии и странах Скандинавского полуострова [9].<br>
  <br>
  Вышеизложенные факты свидетельствуют о неоднородности последней категории. Кто-то 
  в большей степени, кто-то в меньшей мог опасаться мести советской власти, которая 
  в целом ряде случаев (из пропагандистских соображений)стала наказывать только 
  наиболее одиозных «предателей» и прощать (но по своим понятиям, незначительно) 
  рядовых добровольцев. Однако среди «восточных» добровольческих частей были такие, 
  все члены которых, по целому ряду причин, не могли надеяться на снисхождение. 
  Всех их ждала смертная казнь или 25-летняя каторга. Это следующие формирования:<br>
  <br>
  соединения так называемой Русской освободительной армии (РОА);<br>
  <br>
  казачьи части;<br>
  <br>
  формирования из представителей белой эмиграции;<br>
  <br>
  ряд украинских и белорусских соединений;<br>
  <br>
  кавказские и среднеазиатские части и подразделения.<br>
  <br>
  <img src="a-zaburok2.jpg" width="200" height="125"><br>
  <br>
  Следует отметить, что процесс репатриации происходил в несколько «волн», каждая 
  из которых имела свои особенности. Можно выделить три крупные «волны»: 1944—1946, 
  1945—1946 и 1946—1947 годы. Выдачи «русских военнопленных в немецкой военной 
  форме» имели место и до Ялтинских соглашений. Некоторые из них были захвачены 
  в 1943 году в Северной Африке и выданы через Египет и Иран по устной договоренности 
  советского и английского правительств. В 1944 году также поступали с пленными, 
  захваченными после высадки западных союзников в Италии и Северной Франции [10]. 
  Главной особенностью первой «волны» было то, что репатриация происходила небольшими 
  группами и, в основном, морем (например, из Англии в советские порты Мурманск 
  и Одессу). По данным немецкого историка Й. Хоффманна, за период с 1944 по 1946 
  год подобным образом было отправлено 32529 человек — бывших членов добровольческих 
  формирований вермахта [11].<br>
  <br>
  <img src="a-zaburok3.jpg" width="200" height="129"><br>
  <br>
  Третья «волна» имела место уже после начала «холодной войны» и была как бы инерцией 
  двух первых. СССР по инерции продолжал еще требовать выдачи «предателей», а 
  западные союзники по инерции продолжали выполнять эти требования. И это несмотря 
  на то, что еще в июне 1946 года англичане объявили об окончании репатриации 
  со своей территории и даже закрыли соответствующий отдел Военного министерства. 
  Тем не менее, ими были проведены две операции, получившие «поэтические» названия 
  «Килевание» (1946) и «Восточный ветер» (1947). Обе операции проходили в Италии, 
  а количество выданных было минимальным — не более 300 человек [12].<br>
  <br>
  Возможно, первая и третья «волны» так бы и остались неизвестными широким кругам 
  мировой общественности. Возможно, о них бы вспоминали только как о небольшом 
  недоразумении, а их жертвы затерялись бы в общей массе потерь Второй мировой 
  войны. Вероятно, так бы оно и произошло, если бы не вторая «волна». Именно благодаря 
  трагическим событиям, разыгравшимся в 1945—1946 годах, мир узнал, кто такие, 
  по меткому выражению эмигрантского историка Н.Д. Толстого, «жертвы Ялты».<br>
  <br>
  Подчеркнем, что вторая «волна» репатриации наиболее отвечала «букве» Ялтинских 
  соглашений. В результате ее союзнический долг западных демократий перед СССР 
  был выполнен и даже перевыполнен. Но это только с одной стороны. С другой же 
  стороны, выдачи этого периода показали, что исходя из политической конъюнктуры 
  можно «закрыть глаза» на многие юридические и моральные нюансы. И те же демократии 
  были здесь ничуть не лучше, чем «маршал Сталин».<br>
  <br>
  Выдачи этой «волны» начались сразу после капитуляции Германии. Однако в отличие 
  от двух других «волн», здесь Ялтинское соглашение о репатриации было применено 
  к более многочисленным группам «восточных» добровольцев. Они происходили в разное 
  время, были отдалены географически, но объединяло их то, что совершались они 
  всегда обманным путем и с большой жестокостью.<br>
  <br>
  Первыми «жертвами Ялты», испытавшими на себе все «прелести» этого соглашения, 
  стали добровольческие формирования, собравшиеся в мае 1945 года в Каринтии (Австрия). 
  Это Казачий стан генерал-майора Т. Доманова, находившийся в районе Лиенца и 
  насчитывавший 24 тыс. военных и гражданских лиц, группа кавказцев в Обердраубурге 
  под командованием генерала Султан-Келеч Гирея численностью в 4800 человек и 
  15-й Казачий кавалерийский корпус под командованием немецкого генерал-лейтенанта 
  Г. фон Паннвица численностью в 30—35 тыс. человек, находившийся в районе Фельдкирхен 
  — Альтхофен [13].<br>
  <br>
  <img src="a-zaburok4.jpg" width="131" height="200"><br>
  <br>
  Все эти лица надеялись, что их считают военнопленными со всеми вытекающими из 
  этого последствиями. Кроме того, многие попросту не верили, что англичане, верные 
  принципу предоставления «политического убежища» всем, испытывающим притеснения 
  на Родине, выдадут их на верную смерть. Однако, как показали дальнейшие события, 
  нежелание «ссориться с Советами» оказалось сильнее всех традиций «доброй старой 
  Англии».<br>
  <br>
  <img src="a-zaburok5.jpg" width="200" height="201"><br>
  <br>
  Насильственная выдача этих 50—60 тыс. человек была тщательно подготовлена штабом 
  5-го английского корпуса. Причем английское командование относилось к ней настолько 
  серьезно, что во всех соответствующих документах она обозначалась термином «военная 
  операция». Поначалу предполагалось применить только обманный маневр, но вскоре 
  стало ясно, что придется прибегнуть и к насильственным действиям. 28 мая 1945 
  года 2756 офицеров (в том числе 35 генералов) соединения генерал-майора Доманова 
  и 125 кавказских офицеров Султан-Келеч Гирея были отделены от своих подчиненных 
  и семей и под предлогом совещания с представителями английского командования 
  перевезены в строго охраняемый лагерь в Шпиттале. Некоторым из них на другой 
  день, когда обман раскрылся, удалось бежать, некоторые, как генерал-майор Д. 
  Силкин, покончили с собой, многие были застрелены англичанами при попытке к 
  бегству. Но подавляющее большинство 29 мая 1945 года было доставлено в Юденбург 
  и там передано советским представителям [14].<br>
  <br>
  Отделение командного состава послужило прелюдией к назначенной на 31 мая—1 июня 
  1945 года выдаче рядовых казаков и кавказцев в районе Лиенц—Обердраубург. Скрыть 
  от такой массы людей, что их собираются передать советским властям, было уже 
  невозможно. Это и послужило причиной трагедии, которая разыгралась в эти дни 
  в долине р. Драва. По свидетельствам очевидцев (как выживших казаков, так и 
  англичан), наиболее кровавые события произошли в лагере Пеггец. Здесь казаки 
  и их семьи собрались вокруг импровизированного алтаря, наивно полагая, что вид 
  молящихся людей остановит английских солдат. Однако те имели четкий приказ выдать 
  всех, кто был в этом и других лагерях, в руки советских представителей. В результате 
  произошло столкновение, в котором на безоружных казаков сначала пытались «воздействовать» 
  дубинками и прикладами, а потом, когда это не помогло и люди стали разбегаться, 
  многие английские солдаты попросту открыли огонь на поражение. По далеко не 
  полным данным, только в этот день и только в этом лагере погибло около 700 человек, 
  из которых 20—30 утонули в Драве, когда пытались спастись. Остальных же, которых, 
  как пишут очевидцы, охватило какое-то оцепенение и равнодушие, партиями загружали 
  в грузовики и везли на пункт выдачи — в Юденбург. События, подобные этим, происходили 
  и в других лагерях Казачьего стана. Всего же за две недели июня англичане «репатриировали» 
  здесь более 22 тыс. человек [15].<br>
  <br>
  <img src="a-zaburok6.jpg" width="250" height="121"><br>
  <br>
  В те же дни, когда казаки группы Доманова были переданы советским властям, в 
  100 км восточнее вершилась судьба 15-го Казачьего кавалерийского корпуса. Здесь 
  выдачи начались 28 и закончились 30 мая 1945 года. За исключением 50 казаков, 
  которым удалось спастись, весь его личный состав также оказался на станции Юденбург 
  [16].<br>
  <br>
  Выше уже говорилось, что все выдачи второй «волны» сопровождались необычайным 
  коварством и жестокостью со стороны тех, кто выдавал. Однако еще одной особенностью 
  описанных событий было то, что англичане не ограничились репатриацией только 
  советских граждан. Заодно они выдали и тех, кто никак не подпадал под это определение. 
  Речь идет о так называемых старых эмигрантах (так, в отличие от беженцев периода 
  Второй мировой войны называли бывших белогвардейцев) и немецком командном и 
  кадровом персонале казачьих частей. Например, среди выданных офицеров Казачьего 
  стана 1430 были «старыми эмигрантами», среди них бывший атаман Всевеликого войска 
  Донского генерал от кавалерии П. Краснов, генерал-лейтенант А. Шкуро и бывший 
  командир Дикой дивизии Султан-Келеч Гирей. И если эти последние имели хоть какое-то 
  отношение к России (правда, не советской), то 145 немецких офицеров 15-го корпуса 
  такого отношения не имели вовсе [17].<br>
  <br>
  Впоследствии схема выдач казаков в долине Дравы станет «классической» и для 
  других подобных «мероприятий». Как можно увидеть, она была довольно проста. 
  Сначала отделение офицеров от рядового состава, по возможности обманным путем, 
  а затем выдача последнего — обычно с применением силы или методов психологического 
  воздействия (так, чтобы заставить казаков 15-го корпуса сесть в грузовики, англичане 
  продемонстрировали им действие самоходной огнеметной установки «Оса», после 
  чего у многих людей, прошедших всю войну, началась истерика). Несколько позднее, 
  по этой же отработанной схеме были выданы члены еще одного крупного добровольческого 
  формирования — 2-й дивизии РОА, капитулировавшей перед американцами 8—10 мая 
  1945 года в Австрии (1-я дивизия капитулировала несколько раньше перед советскими 
  войсками). Выдача дивизии происходила поэтапно, так как ее личный состав был 
  разбросан по нескольким лагерям: Кемптен (11 августа 1945 года), Дахау (21 января 
  1945 года) и Платтлинг (24 февраля — 13 мая 1946 года). При этом каждая «порция» 
  выдаваемых насчитывала от 500 до 1500 человек. Эти репатриации также происходили 
  в сопровождении кровавых эксцессов, а события в бывшем нацистском лагере Дахау 
  даже стали достоянием мировой общественности. Пленные власовцы (400 человек), 
  узнав о готовящейся выдаче, объявили голодовку и отказались выходить из бараков, 
  многие умоляли американских солдат застрелить их. Однако и здесь сопротивление 
  не вышло за рамки пассивного. Четырнадцать человек покончили с собой отчасти, 
  как сообщается, «чтобы вразумить американцев», двадцать один нанес себе такие 
  тяжелые ранения, что потребовалось стационарное лечение в госпитале. Остальных 
  же, решительно подавив все попытки к сопротивлению, выдали советским представителям 
  [18].<br>
  <br>
  В ходе этой серии репатриаций, как и в случае с казаками, старшие офицеры РОА 
  были отделены от рядовых (их главнокомандующий генерал А.А. Власов был еще 12 
  мая 1945 года пленен советскими войсками) и выданы в последнюю очередь — в Платтлинге 
  [19].<br>
  <br>
  Летом 1945 года произошла репатриация еще одного крупного «восточного» добровольческого 
  формирования — 162-й Тюркской пехотной дивизии. Это соединение капитулировало 
  на севере Италии перед английскими войсками в мае 1945 года, а уже через несколько 
  недель ее личный состав отправили поездом в порт Таранто, а оттуда — на корабле 
  в Одессу. Начало этого «недобровольного путешествия» не предвещало ничего хорошего. 
  Так, перед отправлением один мулла сжег себя в знак протеста против репатриации, 
  а многие пленные утопились в море [20].<br>
  <br>
  <img src="a-zaburok7.jpg" width="250" height="141"><br>
  <br>
  События второй «волны» репатриации показали многое. Например, коварство и жестокость 
  западных демократий, на которые они шли, если дело касалось личных интересов. 
  Представители Англии, США, Франции и других стран охотно выполняли Ялтинские 
  соглашения о репатриации, вплоть до последней запятой и, как было показано выше, 
  даже перевыполняли. Их не смущали протесты таких уважаемых на Западе людей, 
  как генерал А.И. Деникин, обращения ряда церковных и общественных деятелей. 
  Наконец, их не смущал сам факт того, что они отправляют на верную смерть тысячи 
  людей, среди которых имелись даже их союзники по Первой мировой войне (например, 
  генерал П.Н. Краснов). Однако, с другой стороны, эти же события показали, что 
  если репатриации определенной части «восточных» добровольцев могли противоречить 
  каким-то их интересам, они, не задумываясь, оставляли их на Западе. Так, по 
  целому ряду причин, англичане и американцы отказались выдать советским представителям 
  личный состав украинской 14-й и белорусской 30-й гренадерских дивизий войск 
  СС, Русского охранного корпуса и Авиационной группы РОА (за исключением ее командующего 
  генерал-майора В.И. Мальцева). Самый известный из этих эпизодов произошел с 
  1-й Русской национальной армией генерал-майора Б.А. Хольмстон-Смысловского. 
  В мае 1945 года ей удалось прорваться в Лихтенштейн, и правительство этого княжества 
  отказалось выдать ее личный состав, даже несмотря на угрозы советской стороны. 
  Эти события получили такой резонанс, что о них помнят и сейчас: не так давно 
  на их основе был снят художественный фильм «Ветер с Востока» [21].<br>
  <br>
  <img src="a-zaburok8.jpg" width="250" height="174"><br>
  <br>
  <img src="a-zaburok9.jpg" width="200" height="144"><br>
  <br>
  Историки до сих пор гадают, что могло послужить причиной такой избирательности. 
  Тем не менее, всем ясно, что маленький Лихтенштейн не смог бы в одиночку противостоять 
  СССР, впрочем, как и нейтральная Швейцария. Скорее всего, в невыдаче Хольмстона 
  были заинтересованы очень влиятельные люди из разведывательных сообществ Англии 
  и США (дело в том, что этот бывший белогвардеец являлся одновременно и офицером 
  немецкой военной разведки — Абвера). Что касается украинцев и белорусов, то, 
  по официальной версии, за них заступился римский папа Пий XII, так как подавляющее 
  большинство этих добровольцев были католиками или греко-католиками [22].<br>
  <br>
  В послевоенной западной и эмигрантской литературе много говорилось о мотивах 
  и причинах, по которым СССР требовал, а союзники пошли на насильственные репатриации. 
  И, если в случае с советской стороной все более или менее ясно, то о многих 
  побудительных причинах представителей Англии и США можно только догадываться. 
  В целом, по словам английского историка Н. Бетелла, можно выделить следующие:<br>
  <br>
  1. Необходимость обеспечить безопасность английских и американских военнопленных, 
  находившихся в советских руках.<br>
  <br>
  2. Опасения вызвать подозрения советского правительства в неискренности и тем 
  повредить общему делу — ведению войны.<br>
  <br>
  3. Страх перед трудностями, которые вызвала бы необходимость устройства и расселения 
  на Западе большого числа советских граждан [23].<br>
  <br>
  Однако, по словам эмигрантского историка Н.Д. Толстого, к первым двум причинам 
  никто серьезно не относился, так как выдачи продолжались и в 1946, и в 1947 
  годах. Значит, главной причиной все-таки было: «Нам они здесь не нужны».<br>
  <br>
  Итак, в 1943—1947 годах, в ходе всех трех «волн» репатриации западные союзники 
  с применением силы передали СССР более 2,2 млн советских граждан, которые проходили 
  службу в «восточных» добровольческих формированиях германских вооруженных сил 
  (в это число также входят члены семей последних и беженцы, группировавшиеся 
  вокруг тех или иных добровольческих частей). Из них по прибытии в СССР:<br>
  <br>
  • 20 % — расстреляны или осуждены на 25 лет лагерей (что, по сути, было равносильно 
  смертному приговору);<br>
  <br>
  • 15—20 % — осуждены на 5—10 лет лагерей;<br>
  <br>
  • 10 % — высланы в отдаленные районы Сибири не менее, чем на 6 лет;<br>
  <br>
  • 15 % — посланы на принудительные работы в Донбасс, Кузбасс и другие районы, 
  разрушенные немцами. Вернуться домой им разрешалось только лишь по истечении 
  срока работ;<br>
  <br>
  • 15—20 % — разрешили вернуться в родные места.<br>
  <br>
  Как видно, эти данные не дают при сложении 100 %: вероятно, недостающие 15—20 
  % — это люди, которым удалось «скрыться» уже в СССР, умершие в дороге или бежавшие 
  [24].<br>
  <br>
  Эти данные также не дают представления о том, какая судьба постигла каждую из 
  категорий «восточных» добровольцев. Следует признать, что наиболее «круто» советское 
  правосудие обошлось с бойцами и командирами РОА. Согласно данным Н. Краснова, 
  внучатого племянника атамана, офицеры из окружения генерала Власова и штабные 
  офицеры были сразу же отделены от остальных, а прочие власовцы сразу же вывезены 
  в специальный лагерь под Кемерово, где советские компетентные органы начали 
  их фильтрацию на предмет выявления всех офицеров, вплоть до командиров батарей 
  и взводов. При этом наибольшее внимание уделялось офицерам-пропагандистам, прошедшим 
  подготовку на курсах в Дабендорфе (вероятно, как носителям власовской идеологии). 
  Большинство из них были приговорены трибуналами Восточно-Сибирского военного 
  округа к расстрелу, а остальные получили сроки в лагерях, чаще всего по 25 лет 
  (главным образом на Колыме, в Воркуте и Джезказгане) [25].<br>
  <br>
  Нечто похожее ожидало солдат и офицеров казачьих и других формирований. Со временем, 
  к 1946 году, советские органы перестали различать отдельные категории «восточных» 
  добровольцев, и по всем официальным и неофициальным документам они стали проходить 
  как «власовцы».<br>
  <br>
  Что же касается командующих этих формирований, то их судьба была предрешена 
  заранее. Несмотря на судебные разбирательства (правда, закрытые), все они были 
  приговорены к смертной казни через повешение. Сначала генерал-лейтенант А. Власов 
  и его соратники, а затем атаман П. Краснов и другие казачьи генералы. Вместе 
  с последними был также повешен и генерал-лейтенант Г. фон Паннвиц — немецкий 
  дворянин и командир 15-го Казачьего кавалерийского корпуса [26].<br>
  <br>
  Выше уже было сказано, что проблема репатриаций обросла со временем целым клубком 
  вопросов (политических, юридических, моральных и пропагандистских). Они начали 
  возникать уже в ходе этих насильственных выдач. Еще больше их появилось в послевоенный 
  период, который, как известно, проходил под знаком «холодной войны» СССР и Запада. 
  На ее фоне все эти вопросы переплелись настолько, что их порой бывает трудно 
  отделить друг от друга. Выделим только главные и постараемся вкратце на них 
  ответить.<br>
  <br>
  <img src="a-zaburok10.jpg" width="300" height="107"><br>
  <br>
  Мы уже упоминали один из этих вопросов: почему западные демократии пошли на 
  такое соглашение с СССР? Просто так сложилась политическая конъюнктура и, как 
  говорится, «ничего личного…». Однако со временем к этому вопросу добавился еще 
  один: как соглашения о репатриации, достигнутые в Ялте, согласуются с нормами 
  международного права, а именно с Женевской конвенцией (1929 года) о правах военнопленных. 
  Дело в том, что в ней ясно и четко сказано, что национальность солдата определяется 
  исключительно его военной формой. В армии Великобритании воевало много иностранцев 
  — беженцев из Германии и оккупированных ею стран, многие из которых были гражданами 
  государств нацистского блока. Всех их следовало уберечь от опасностей, связанных 
  с пленом. Поэтому на ранней стадии войны Великобритания недвусмысленно заявила 
  в Берлине через свое государство-протектор, что всякий солдат английской армии, 
  попав в плен, находится под защитой Женевской конвенции. И пока существовала 
  реальная опасность немецких репрессий для английских солдат, Великобритания 
  неукоснительно придерживалась «принципа формы», хотя бы внешне. До 1943 года 
  это касалось и «русских солдат в немецкой форме». Однако когда опасность немецких 
  репрессий миновала, англичане, опять-таки из политических соображений, стали 
  рассматривать «восточных» добровольцев не как военнопленных, а как предателей 
  союзной державы и обращаться с ними соответствующим образом [27].<br>
  <br>
  Поэтому, отвечая на данный вопрос, можно сказать, что выдача членов добровольческих 
  формирований, которые являлись солдатами вермахта, носили немецкую военную форму 
  и, следовательно, находились под защитой Женевской конвенции, означала явное 
  нарушение действующего военного права. Однако и тут не все так просто. Если 
  под статьи Женевской конвенции легко подпадали бойцы многочисленных «восточных» 
  батальонов и вспомогательный персонал немецких частей и соединений (так называемые 
  хиви — «добровольные помощники»), то с солдатами РОА и других подобных формирований 
  дело обстояло намного труднее. Ведь многие из них считали себя (и вполне искренне) 
  не немецкими прислужниками, а бойцами национальной армии, которая борется за 
  освобождение своей Родины (наподобие хорошо известного в российской истории 
  Чехословацкого легиона времен Первой мировой и гражданской войны). Было ли уместно 
  обвинение этой сравнительно небольшой группы в измене Родине? По мнению немецкого 
  историка Й. Хоффманна, это обвинение не выдерживает никакой критики. «Следует 
  заметить, — пишет он, — что понятие “измена Родине” может относиться лишь к 
  отдельным лицам или незначительным по численности группам. Но когда в вооруженном 
  конфликте… миллион солдат активно воюет на стороне противника, речь идет уже 
  не об “измене Родине”, а о некоем политико-историческом процессе» [28]. Однако 
  как отделить тех, кто пошел в РОА и подобные ему национальные формирования сознательно, 
  от тех, кто был загнан туда силой? Западные союзники не стали ломать над этим 
  голову.<br>
  <br>
  В целом, ведущая роль в политике выдач принадлежала Великобритании, а США последовали 
  за ней, хотя и не без колебаний и в меньшем масштабе применяя насилие. И если 
  англичане выдавали всех подряд, и бывших «красных», и бывших «белых», то американцы 
  пытались подходить к ним дифференцированно. Выше уже было сказано, что репатриации 
  подлежали только советские граждане по состоянию на 1 сентября 1939 года. То 
  есть жители Западной Украины, Западной Белоруссии и Прибалтики не являлись таковыми 
  в глазах американцев. И в основном они этого правила придерживались. Кровавые 
  же эксцессы в лагере Дахау настолько испугали американское командование, что 
  оно решило на некоторое время прекратить массовые выдачи и отправлять военнопленных 
  в СССР только после подробного опроса. Так, в штабе 3-й армии был подготовлен 
  ряд вопросов, на основании которых американцы пытались разделить возможных репатриантов 
  на полноправных граждан и тех, кто подвергался преследованиям на Родине и потому 
  теперь не подлежит выдаче. Вопросы касались, например, права носить оружие, 
  права участвовать в свободных выборах или права занимать общественный пост. 
  В конце концов, американские репатриационные комиссии выделили несколько групп 
  людей, не обладавших, на их взгляд, гражданскими правами и тем самым не являвшихся 
  советскими гражданами, — «кулаки», «белые» и «диссиденты». Однако и здесь не 
  обошлось без недоразумений. Например, те, кто заявлял, что вступил в РОА под 
  нажимом немцев или под угрозой голодной смерти, подлежали выдаче в первую очередь 
  (в лагере Платтлинг таких оказалось более 3 тыс. человек) [29].<br>
  <br>
  Но такие, даже неполовинчатые, меры не могли удовлетворить советскую сторону. 
  И поэтому под ее давлением, а также при деятельном участии Великобритании США 
  были вынуждены сдаться. Результатом этой капитуляции стала так называемая Директива 
  Мак-Нарни—Кларка (21 декабря 1945 года). Согласно этому документу, обязательной 
  репатриации, «независимо от желания и с применением силы, если это окажется 
  необходимо», подлежали все:<br>
  <br>
  1. Взятые в плен в немецкой форме.<br>
  <br>
  2. Находившиеся в рядах советских вооруженных сил 22 июня 1941 года и после 
  этой даты и не демобилизованные впоследствии.<br>
  <br>
  3. Обвиняемые советскими властями в добровольной помощи врагу, при предоставлении 
  убедительных доказательств с советской стороны [30].<br>
  <br>
  И тем не менее, это была хоть и капитуляция, но капитуляция почетная: нечто 
  среднее между полным отказом от репатриации и безоговорочными английскими выдачами. 
  По замыслу американских юристов директива была принята с целью обеспечить возвращение 
  предполагаемых предателей, с тем чтобы они получили заслуженное наказание, тогда 
  как вопрос об обычных беженцах, не запятнавших себя сотрудничеством с врагом, 
  должен был рассматриваться в соответствии с традиционной американской политикой.<br>
  <br>
  Наконец, после начала «холодной войны» этот вопрос приобрел и некоторое пропагандистское 
  звучание. Как бы неожиданно «свободный мир» узнал о том, что Ялтинские соглашения 
  о репатриации были настоящей трагедией для миллионов. И это несмотря на то, 
  что к ней в свое время старались привлечь внимание, как уже говорилось, и папа 
  римский, и видные деятели зарубежной Православной церкви, да и сами «восточные» 
  добровольцы в лице своих лидеров (например, письма генерал-майора М. Меандрова 
  к г-же Рузвельт). С другой же стороны, на Западе узнали, что во время войны 
  в СССР было, оказывается, более миллиона активных борцов с режимом. Все это, 
  конечно, стало актуальным не из-за каких-то моральных побуждений. Просто все 
  эти лица, как полагали западные лидеры, могут стать действенной «пятой колонной» 
  во время предполагаемой войны с СССР. Кроме того, к 50-м годам прошлого века 
  относится и начало активного изучения в европейской и американской историографии 
  проблемы коллаборационизма в Советском Союзе в период Второй мировой войны. 
  Причем наиболее серьезно изучались все вопросы, связанные с немецкими методами 
  по вербовке «восточных» добровольцев, и те ошибки, которые они допустили, не 
  дав развернуться так называемому Освободительному движению народов России [31].<br>
  <br>
  Сама же тема насильственной репатриации стала одним из сюжетов пропагандистской 
  войны Запада против СССР. Например, в знаменитом «Архипелаге ГУЛАГ» А. Солженицына 
  ее жертвам посвящена целая глава под названием «Та весна» [32]. А многие бывшие 
  власовцы и члены других добровольческих формирований, выйдя из лагерей по амнистии 
  1955 года, пополнили ряды зарождавшегося в СССР диссидентского движения.<br>
  <br>
  Когда 60 лет назад главы трех союзных держав подписывали в Ялте соглашения о 
  репатриации, они и представить себе не могли, что она переживет их всех и будет 
  служить объектом пристального изучения даже в новом, XXI веке. И если Сталин 
  действительно понимал, зачем нужны ему эти репатрианты, то Рузвельт и Черчилль, 
  кажется, не отдавали себе в этом отчета. Предпринимая попытку объективно подойти 
  к этой проблеме, автор, естественно, не собирался обелять настоящих предателей 
  и военных преступников. Многие из них, как впоследствии оказалось, все-таки 
  остались на Западе и просто поменяли хозяев. Что же касается обычных беженцев, 
  обманутых или даже тех, кто «добросовестно заблуждался», считая нацизм меньшим 
  злом, чем коммунизм, то их выдача была аморальной с любой точки зрения. Разве 
  что только не с политической?! Кубанский атаман В. Науменко назвал эти выдачи 
  «великим предательством». С ним нельзя не согласиться. Но, на наш взгляд, это 
  было предательство не в буквальном смысле этого слова. Именно там, в долине 
  Дравы, Кемптене, Дахау, Платтлинге и многих других местах, русский человек в 
  очередной раз убедился в том, чего стоят все заверения Запада о демократии, 
  правах человека и тому подобных вещах. К сожалению, этот один из многочисленных, 
  незаслуженно забытых, но не последних по значению уроков Ялты не пошел ему впрок.<br>
