  <br>
  Песня Гайворона (актер Петр Алейников) - одного из главных героев фильма, морячка-анархиста, 
  перешедшего в Красную армию к Пархоменко. По украинской степи после победного 
  боя едет красная кавалерия, впереди - Пархоменко, с ним рядом - поющий под гармонь 
  Гайворон, далее - знамя и остальные бойцы, которые хором подхватывают последние 
  строки куплетов.<br>
  <br>
  Фильм Леонида Лукова &quot;Александр Пархоменко&quot;, вышедший на экраны в 1942 году, посвящен событиям Гражданской войны на 
  Украине:  боях против немцев, а затем против белых и махновцев. Поначалу в числе врагов были поляки, но в ходе съемок СССР заключил дипломатические отношения с польским эмигрантским правительством, и бои с поляками в фильм не вошли. Съемки заканчивались,  когда Украина была оккупирована 
  фашистами, поэтому немцы в фильме выглядят наиболеее комично. В начале фильма 
  в центре внимания - бои за Царицын (будущий Сталинград), который красные в годы 
  Гражданской войны удержали и не сдали белым. Самым ярким персонажем получился 
  не демонстративно положительный Пархоменко, а гротескные &quot;украинские бандиты&quot; 
  - &quot;командующий армией Анархии батька Максюта&quot;, атаман Григорьев и, 
  разумеется, батька Махно (актер Борис Чирков), играющий на гармошке, пьющий 
  самогонку и поющий <a href="../../kazaki/lubo.php">&quot;Любо, братцы, любо&quot;</a>. 
  В фильме, по сути, только два подлинных факта - убийство махновцами 
  атамана Григорева и гибель Пархоменко 3 января 1921 года, хотя в кино он гибнет 
  в открытом победном бою, а в жизни погиб случайно - был зарублен махновской 
  разведкой, которую принял за своих.<br>
