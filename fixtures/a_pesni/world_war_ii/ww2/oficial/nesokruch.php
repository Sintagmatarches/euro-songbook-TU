<span class='bold'>ВАРИАНТЫ (2)<br>
<br>
1. Несокрушимая и легендарная<br></span>
<br>
Музыка Александра Александрова<br>
Слова Осипа Колычева<br>
<br>
Над страною шумят как знамена<br>
Двадцать семь героических лет.<br>
Отзвук славных боев отдаленных,<br>
Весь твой путь в наших песнях воспет.<br>
<br>
<span class='i'>Припев:</span><br>
<br>
Несокрушимая и легендарная,<br>
В боях познавшая радость побед -<br>
Тебе любимая, родная армия<br>
Шлет наша Родина песню - привет.<br>
<br>
Родилась ты под знаменем алым<br>
В восемнадцатом грозном году.<br>
Всех врагов ты всегда сокрушала,<br>
Победишь ты фашистов орду.<br>
<br>
Ленинград мы в боях отстояли,<br>
Отстояли родной Сталинград.<br>
Нас ведет в наступление Сталин,<br>
Наши танки фашистов громят!<br>
<br>
Победим - наша сила несметна,<br>
Гений Сталина в бой нас ведет.<br>
Наша армия в битвах бессмертна,<br>
Как бессмертен Советский Народ.<br>
	<br>
<span class='i'>Неизвестный источник. Дата: 1945</span><br>
<br>
<br>
<span class='bold'>2. Песня о Советской Армии</span><br>
<br>
Музыка Александра Александрова<br>
Слова Осипа Колычева<br>
<br>
Над тобою шумят, как знамена,<br>
Годы наших великих побед.<br>
Солнцем славных боев озаренный,<br>
Весь твой путь в наших песнях воспет.<br>
<br>
<em>Припев:</em><br>
<br>
Несокрушимая и легендарная,<br>
В боях познавшая радость побед,<br>
Тебе, любимая, родная армия,<br>
Шлет наша Родина песню-привет!<br>
<br>
Родилась ты под знаменем алым<br>
В восемнадцатом грозном году,<br>
Всех врагов ты всегда побеждала,<br>
Победила фашистов орду.<br>
<br>
Нашей Партии гордое знамя<br>
Нас на подвиг ведет за собой<br>
И, как солнце, сияет над нами,<br>
Над великой Советской страной.<br>
<br>
Процветай же, родная держава,<br>
И победная песня, звени!<br>
В дни боев умножали мы славу,<br>
Приумножим и в мирные дни.<br>
<br>
<span class='i'>Русские советские песни (1917-1977) / Сост. Н. Крюков и Я. Шведов. 
М.: Худож. лит., 1977. Дата: 1943.<br>
</span><br>
