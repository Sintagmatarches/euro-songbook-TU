    Одна из самых известных песен Великой Отечественной войны. Опубликованное стихотворение  стихийно 
    исполнялось на фронте на разные мелодии, из которых устоялась одна - мелодия 
    танго. На этот же мотив поется известная с 1939 года песня польского Сопротивления 
    <a href="../../ww2-polsk/wolnoscprzyn.htm">&quot;Wolność przynieść Ojczyźnie 
    chcąc&quot;</a> (фонограмма - <a href="http://www.youtube.com/watch?v=klJcMviVorw">http://www.youtube.com/watch?v=klJcMviVorw</a>). 
    Встречается мнение, что это мелодия танго 1930-х гг. композитора Ежи Петерсбурского 
    <a href="../../popular-in/polska/juznigdy.htm">&quot;Już nigdy&quot;</a> (&quot;Уже 
    никогда&quot;, фонограмму см. <a href="http://www.youtube.com/watch?v=PwxRvmhzulA&feature=related">http://www.youtube.com/watch?v=PwxRvmhzulA&amp;feature=related</a>) 
    - но у него другая мелодия. Так что в основе, видимо, лежит какое-то другое 
    популярное польское танго. Музыку к стихотворению писали также Матвей Блантер 
    и другие композиторы, но их версии не прижились.<br>
    <br>
