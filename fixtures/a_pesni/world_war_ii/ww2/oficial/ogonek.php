<h1>ОГОНЕК</h1>

    Слова Михаила Исаковского<br>
    <br>
    На позиции девушка<br>
    Провожала бойца,<br>
    Темной ночью простилася<br>
    На ступеньках крыльца.<br>
    И пока за туманами<br>
    Видеть мог паренек,<br>
    На окошке на девичьем<br>
    Все горел огонек.<br>
    <br>
    Парня встретила дружная<br>
    Фронтовая семья,<br>
    Всюду были товарищи,<br>
    Всюду были друзья,<br>
    Но знакомую улицу<br>
    Позабыть он не мог:<br>
    &quot;Где ж ты, девушка милая,<br>
    Где ж ты, мой огонек?&quot;<br>
    <br>
    И подруга далекая<br>
    Парню весточку шлет,<br>
    Что любовь ее девичья<br>
    Никогда не умрет.<br>
    Все, что было задумано,<br>
    В свой исполнится срок,<br>
    Не погаснет без времени<br>
    Золотой огонек.<br>
    <br>
    И становится радостно<br>
    На душе у бойца<br>
    От такого хорошего<br>
    От ее письмеца.<br>
    И врага ненавистного<br>
    Крепче бьет паренек<br>
    За советскую Родину,<br>
    За родной огонек.<br>
    <br>
    слова - 1942<br>
