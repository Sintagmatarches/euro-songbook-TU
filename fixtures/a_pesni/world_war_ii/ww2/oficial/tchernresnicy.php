1. Ты стояла молча ночью у вокзала...</span><br>
<br>
Ты стояла молча ночью у вокзала, <br>
На глазах нависла крупная слеза. <br>
Видно, в путь далекий друга провожала — <br>
Черные ресницы, карие глаза. <br>
<br>
Вот помчался поезд, рельсы отгудели, <br>
Милый друг уехал, может, навсегда, <br>
И с тоской немою вслед ему смотрели <br>
Черные ресницы, карие глаза. <br>
<br>
Он недаром кончил школу боевую, <br>
Метко поражает подлого врага <br>
И не забывает девушку простую — <br>
Черные ресницы, карие глаза. <br>
<br>
Редко пишет письма, но зато в газетах <br>
Часто вспоминают про его дела, <br>
И, о нем читая, гордостью сияют <br>
Черные ресницы, карие глаза. <br>
<br>
Он взвивался птицей, грозный смелый сокол, <br>
Немцы его знают, он для них гроза, <br>
О таком в народе песню сочиняют — <br>
Черные ресницы, карие глаза.<br>
