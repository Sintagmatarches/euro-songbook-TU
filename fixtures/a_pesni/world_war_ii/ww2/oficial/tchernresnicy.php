<h1>ЧЕРНЫЕ РЕСНИЦЫ, ЧЕРНЫЕ ГЛАЗА</h1>

Музыка Альберта Гарриса<br>
Слова Владимира Ильченко<br>
<br>
Ты стояла молча ночью на вокзале, <br>
На глазах нависла крупная слеза. <br>
Видно, в путь далекий друга провожали<br>
Черные ресницы, черные глаза. <br>
<br>
Вот умчался поезд, рельсы отгудели, <br>
Милый друг уехал, может, навсегда.<br>
И с тоской немою вслед ему глядели <br>
Черные ресницы, черные глаза. <br>
<br>
Он недаром кончил школу боевую, <br>
Метко поражает лютого врага <br>
И не забывает девушку простую — <br>
Черные ресницы, черные глаза. <br>
<br>
Пишет письма редко, но зато в газетах <br>
Пишут очень часто про его дела.<br>
И, о нем читая, гордостью сияют <br>
Черные ресницы, черные глаза. <br>
<br>
Сокол краснозвездный в небо вылетает, <br>
Для врагов-фашистов страшен, как гроза.<br>
О таких в народе песни сочиняют,<br>
О таких мечтают черные глаза.<br>
