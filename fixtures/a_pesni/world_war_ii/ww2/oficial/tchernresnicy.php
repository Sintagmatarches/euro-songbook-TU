Милый мой уехал, может, навсегда.<br>
И с тоской-печалью вслед ему глядели<br>
Черные ресницы, черные глаза.<br>
<br>
Милый мой на фронте где-то под Москвою<br>
Пулеметом метким повстречал врага.<br>
Строчит за Отчизну, за село родное,<br>
За свою невесту – черные глаза.<br>
<br>
