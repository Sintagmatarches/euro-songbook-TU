<br>
Альберт Гаррис - польский джазист, после ликвидации Польши оказался в СССР. С 
1940 пел в оркестре Генриха Варса, а когда тот распался в начале войны - в оркестре 
Эдди Рознера, где также писал музыку. Автор мелодий популярных песен 1940-х годов 
<a href="../../drugije/horvstepi.php">&quot;Ковбойская&quot;</a> и <a href="../../drugije/mandolina.php">&quot;Мандолина, 
гитара и бас&quot;</a> (записаны на пластинки в 1944 г.; до 1944 г. оркестр Рознера 
не записывался на пластинки). В 1945 тайно вылетел в Польшу. <br>
<br>
Судя по тексту песни, а также по ее фольклорным записям (в одной из них упоминаются 
бои под Москвой), она написана в начале войны. Более поздняя песня К. Листова 
и С. Алымова <a href="dumka.htm">&quot;Думка&quot;</a> (1945) явно создана под 
влиянием &quot;Черных ресниц&quot;:<br>
<br>
<span class='i'>На Кубани есть одна станица,<br>
Над водой – высокая лоза.<br>
В той станице – черные ресницы,<br>
В тех ресницах – синие глаза...<br></span>
