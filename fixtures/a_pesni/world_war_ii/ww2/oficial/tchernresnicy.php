Но не забывает девушку простую –<br>
Черные ресницы, черные глаза.<br>
<br>
Сокол сизокрылый в небо улетает,<br>
Для врагов проклятых – сильная гроза.<br>
О таких в народе песни сочиняют.<br>
Черные ресницы, черные глаза…<br>
