	<br>
        <span class="text"><strong>Александр Петрушин</strong><br>
        <br>
        <span class="header"><strong>ИЗ ОКРУЖЕНИЯ</strong></span> <br>
        <br>
        <em>&quot;Тюменский курьер&quot;, №26(3043), 16 февраля 2011, №27(3044), 
        17 февраля 2011.</em><br>
        <br>
        <br>
        <em><strong>22 июня 2011 года исполняется 70 лет с начала Великой Отечественной 
        войны. Так случилось, что в соединениях Красной армии, первыми принявших 
        на себя вражеский удар, было немало наших земляков, которые призваны на 
        военную службу в 1939-м, 1940-м и весной 1941 года. Многие служили в 137-й 
        стрелковой дивизии.</strong></em><br>
        <br>
        <strong><br>
        Лучшие из лучших</strong><br>
        <br>
        Это соединение было сформировано в сентябре 1939-го в Арзамасе на базе 
        51-го Иваново-Вознесенского полка 17-й Нижегородской имени ЦИК БССР стрелковой 
        дивизии.<br>
        <br>
        Этот город, по словам родившегося в нем писателя Аркадия Гайдара, «был 
        тихий, весь в садах, огороженных ветхими заборами. В тех садах росло великое 
        множество вишни, яблок-скороспелок, терновника и красных пионов. Сады, 
        примыкая один к другому, образовывали сплошные зеленые массивы, неугомонно 
        звеневшие пересвистами синиц, щеглов, снегирей и малиновок... Через город, 
        мимо садов, тянулись тихие зацветшие пруды. Под горою текла речонка Теша».<br>
        <br>
        В состав дивизии входили три стрелковых полка (771-й, 624-й, 409-й) и 
        два артиллерийских - 497-й гаубичный и 278-й легкий, а также специальные 
        части: истребительно-противотанковый и зенитно-артиллерийский дивизионы, 
        батальоны связи, саперный, разведывательный, автомобильный, медико-санитарный.<br>
        <br>
        Все подразделения были укомплектованы призывниками - волжанами и сибиряками.<br>
        <br>
        Дивизия отличилась на летних маневрах 1940 года, показательных для высшего 
        командования Красной армии: маршалов Тимошенко, Буденного, Шапошникова, 
        генерала армии Мерецкого, армейского комиссара 1-го ранга Мехлиса.<br>
        <br>
        За отлично спланированные и проведенные наркомовские учения командира 
        137-й стрелковой дивизии полковника Гришина наградили орденом Красной 
        Звезды.*<br>
        <br>
        В архиве Министерства обороны России хранится выступление полковника Гришина 
        на совещании у наркома обороны Тимошенко в декабре 1941 года:<br>
        <br>
        «... Произведенные Вами смотровые учения в нашей дивизии заложили прочную 
        основу подготовки частей и создали условия для нашей плодотворной работы 
        в дальнейшем. Правда, у меня нет опыта в командовании дивизией такого, 
        как рассказывал командир 99-й дивизии...»<br>
        <br>
        Передовой в Красной армии 99-й стрелковой дивизией командовал полковник 
        Власов. Тот самый, который 12 июля 1942-го, уже в звании генерал-лейтенанта, 
        командуя окруженной в новгородских лесах и болотах 2-й Ударной армией, 
        сдался в плен и стал сотрудничать с оккупантами «для ведения вооруженной 
        борьбы с Советской властью».**<br>
        <br>
        <strong><br>
        Подняты по тревоге</strong><br>
        <br>
        Мирную жизнь Гороховецкого лагеря, где летом 1941-го располагалась 137-я 
        стрелковая дивизия, прервала война. Бывший начальник оперативного отдела 
        штаба 20-го стрелкового корпуса, в состав которого входила 137-я, И.А. 
        Суетин вспоминал: «В ночь на 22 июня я был дежурным по штабу корпуса. 
        Рано утром из Московского военного округа поступило сообщение, что фашистская 
        Германия напала на нашу страну. Так в Горьком мне довелось первому узнать, 
        что началась война. Сразу же были подняты по тревоге командиры. Только 
        закончили совещание, вскрыли мобилизационный пакет -Молотов выступает 
        по радио.»<br>
        <br>
        Связист 771-го стрелкового полка А.М. Самойленко: «... На утро 22 июня 
        было назначено общее построение полка. Все мы стояли в шеренге по два, 
        ожидая строевого смотра каким-то большим начальником. Прошло более часа, 
        но ни один командир, даже младший лейтенант, не появился. Я вызвался узнать, 
        в чем дело. «Беги, - сказал кто-то в строю, - ты ведь у нас победитель 
        полковых и дивизионных соревнований. Я, и правда, занимал первое место 
        в полку и второе в дивизии по бегу. Быстро прибежал к штабу полка и вижу, 
        как из него выходят молчаливые, озабоченные командиры и поворачивают головы 
        к тарелке громкоговорителя. Я выслушал речь Молотова и побежал к батальону. 
        Все бойцы так и стояли в строю: дисциплина тогда была строгой. Но, когда 
        люди выслушали мой сбивчивый пересказ услышанного по радио, куда девалась 
        дисциплина. Все начали расходиться, а вскоре у палаток появились маленькие 
        костры - жгли письма, бумаги, какие-то вещи, которые не могли взять на 
        войну, даже чемоданы.»<br>
        <br>
        Телефонист 246-го отдельного батальона связи дивизии А.А. Коробков: «А 
        мы в двенадцать часов оказались в столовой. Как услышали, что война, у 
        нас и ложки попадали...»<br>
        <br>
        После совещаний, митингов, партийных и комсомольских собраний из штаба 
        корпуса пришел приказ - возвратиться к месту постоянной дислокации.<br>
        <br>
        Связист 771-го стрелкового полка А.М. Самойленко: «Вечером 22 июня наш 
        полк из Гороховецких лагерей пешком пошел в Горький, в Красные казармы. 
        Сначала шли организованно всем полком, но постепенно растянулись. Командиры 
        поторапливали, но никто не реагировал: каждый ушел в себя, думая о начавшейся 
        войне.<br>
        <br>
        Медленно идут час за часом, в пути накаливается усталость. Одни засыпали 
        на ходу, другие падали где попало во время коротких привалов, третьи начинали 
        изобретать болезни. Были среди нас и другие ребята - выносливые, сильные, 
        горевшие желанием скорей попасть на фронт. Показалась деревня, на ее окраине 
        стоял колодец. Один или два красноармейца бросились к воде, но их остановили: 
        «Товарищи, имейте выдержку, на нас смотрит народ». Действительно, полдеревни 
        собралось у крайнего дома - женщины с детьми. Вдалеке показались отставшие 
        однополчане. Чем ближе они подходили, тем неприглядней картина: многие 
        едва плелись, а такие молодцы! Кто-то натер ноги и теперь поднимал пыль 
        голыми пятками. А как кинулись к колодцу - всей толпой, лезут головами 
        в корыто. Сзади подходят другие, толчея еще больше. Первые признаки войны, 
        с ее трудностями и падением нравов.»<br>
        <br>
        С 26 июня 1941-го эшелон за эшелоном из Горького, Арзамаса, Мурома и Саранска 
        отправлялись на запад полки 137-й стрелковой дивизии.<br>
        <br>
        Ветеринарный врач 624-го стрелкового полка Н.А. Набель: «Напряжение отправки 
        нарастало с каждым часом. При погрузке в вагоны большинство бойцов и командиров 
        прощались со своими родными и знакомыми. Всюду плакали и причитали. На 
        команду «По вагонам!» реагировали далеко не все. Паровоз пыхтел и свистел, 
        призывая к посадке, но проводы продолжались. Наконец, эшелон тронулся 
        и пошел вперед. Женщины бегут к вагонам, многие плачут навзрыд. Такого 
        всеобщего плача я никогда еще не слышал, он долго словно висел в воздухе.»<br>
        <br>
        В 137-й стрелковой дивизии насчитывалось тогда свыше 14 тысяч человек, 
        около трех тысяч лошадей, до 200 орудий и минометов, сотни автомашин, 
        тракторов, повозок. Чтобы перебросить это соединение на фронт, потребовалось 
        36 эшелонов.<br>
        <br>
        <br>
        <strong>Первый бой</strong><br>
        <br>
        На редких остановках все жадно слушали сводки Совинформбюро, а они все 
        тревожней. Оставлены Гродно, Барановичи, Минск. Навстречу все чаще санитарные 
        поезда. Все ближе фронт.<br>
        <br>
        Н.А. Набель: «Шесть часов утра, погожее летнее утро, почти все еще спят. 
        Вдруг резкий грохот потряс состав. Я выскочил из вагона, и, падая на землю, 
        заметил в небе два немецких самолета. Одна бомба угодила в вагон, где 
        ехали семьдесят пять человек, вторая - в теплушку с лошадьми, третья ударила 
        в рельс второго пути, да так, что кусок рельса вырвало, а концы загнулись, 
        как салазки. Из эшелона выскакивают бойцы, бегут врассыпную в лес. Когда 
        самолеты улетели, я пошел в голову состава, где упали бомбы. В вагоне 
        с людьми бомба пробила крышу и пол, кругом изуродованные трупы. В конском 
        вагоне месиво из мяса и костей. Одну лошадь выбросило взрывом на рельсы, 
        у нее нет передней ноги и разворочен живот. Надо пристрелить беднягу, 
        но не могу. Подходит лейтенант: «Ну что смотришь, стреляй!» - «Да вот, 
        пистолет заело!» Наконец выстрелил. До сих пор в памяти глаза этой лошади, 
        полные слез.»<br>
        <br>
        Вечером 29 июня первые эшелоны с частями 137-й стрелковой дивизии начали 
        прибывать на станцию Орша.<br>
        <br>
        Начальник штаба 771-го стрелкового полка А.В. Шапошников: «Орша горела. 
        В воздухе то и дело появлялись немецкие самолеты. Надо было разгружать 
        эшелон. С трудом нашел коменданта станции. Обстановки он не знал, где 
        фронт - неизвестно. Комендант был совершенно издерган обступившими его 
        военными и штатскими. Только махнул мне рукой: «Разгружайтесь, как хотите». 
        Вернулся к эшелону, доложил обстановку командиру полка. Полковник Малинов 
        приказал разгружаться.»<br>
        <br>
        Помощник начальника оперативного отдела штаба 137-й стрелковой дивизии 
        В.К. Реутов: «Нервотрепка и напряжение были страшными. Представьте себе 
        положение полковника Гришина: неизвестность, фронт приближается, нужно 
        собрать дивизию в кулак, а ее эшелоны растянулись на сотни километров 
        - голова была в Орше, а хвост еще только выехал из Саранска.»<br>
        <br>
        Обстановка на Западном фронте была очень тяжелой. На участке Орша - Могилев 
        действовала 2-я танковая группа генерала Гудериана в составе восьми танковых 
        и моторизованных дивизий. Закончив операции в районе Минска 3 июля, эти 
        соединения форсировали Березину и за пять суток преодолели расстояние 
        до Днепра, примерно 90 километров. Здесь им противостояли отступавшие 
        с боями от Минска и понесшие большие потери дивизии 13-й армии, которой 
        командовал генерал-лейтенант Петр Филатов. Он погиб под бомбами авиации 
        противника 8 июля, когда танки Гудериана уже вышли к Днепру. В командование 
        13-й армией вступил генерал-лейтенант Федор Ремезов. По его приказу 137-я 
        стрелковая дивизия должна была сняться со своих позиций у Орши, пешим 
        маршем выйти в район села Сухари, восточнее Могилева, и занять оборону 
        по реке Реста. Лейтенант Скворцов, командир взвода связи 771-го стрелкового 
        полка писал в дневнике об этом переходе: «Страшная жара, пыль, самолеты. 
        Несмотря на двухдневную бессонницу, бойцы чувствуют себя бодро. Эх, и 
        русский солдат!»<br>
        <br>
        В 5 часов утра 10 июля немцы начали форсирование Днепра в районе Быхова 
        - пошел отсчет Смоленского сражения. К отражению переправы противника 
        штаб 13-й армии оказался не готов. Контратака наших войск была назначена 
        на утро 12 июля. Приказ на выступление из Сухарей полковник Гришин получил 
        только вечером 11 июля. Предстояло пройти свыше 30 километров. Ездовые 
        подгоняли упряжки лошадей с орудиями. Пехота то и дело переходила на бег. 
        Все лишнее имущество и снаряжение, даже шинели и вещмешки оставили в Сухарях. 
        До начала боев оставались считанные часы, а дивизию так и не удалось собрать 
        в полноценную боевую единицу. Часть батарей артполков и стрелковых батальонов, 
        выгруженных на перегоне Кричев - Орша, были подчинены командирам других 
        дивизий. Отдельный зенитно-артиллерийский дивизион попал под бомбежку 
        немецкой авиации и понес такие потери, что фактически перестал существовать. 
        Саперный батальон еще до войны был взят на учебные сборы в Малоярославец, 
        и в 137-ю стрелковую дивизию так и не прибыл. В пути на фронт «заблудился» 
        медсанбат - добрался только до Рославля, где попал в другую армию. Нарушение 
        четкой работы железных дорог и налеты вражеской авиации - все это вело 
        к неразберихе и перемешиванию воинских частей.<br>
        <br>
        Для личного руководства контрударом в войска выехал командарм-13 Ремезов. 
        На рассвете 13 июля его автомашину обстреляли немецкие мотоциклисты. Раненого 
        генерала отправили в тыл. В командование 13-й армией вступил генерал-лейтенант 
        Василий Герасименко, третий командующий за неделю.<br>
        <br>
        Тогда же батальоны 1 37-й стрелковой дивизии вышли на исходный рубеж для 
        атаки. Впереди был уже только враг.<br>
        &nbsp;<br>
        Командир отделения 624-го стрелкового полка А.К. Кучинский: <br>
		<br>
        «Пошли мы на сближение с противником. Через рощу вдоль дороги - перебежками 
        дальше, и вдруг по нам ударили пулеметы. Разорвался один снаряд, потом 
        другой. Залегли, пытаемся вести наблюдение, но ничего не видно, да и немецкий 
        пулемет строчит так, что головы не поднять. Решили сменить позицию, выдвинулись 
        на ржаное поле. Окопались. Так день и прошел - перебежки, перестрелки. 
        У меня сохранилась записная книжка: «Первый бой. Из 53 человек нашего 
        взвода в живых осталось 19...»<br>
        <br>
        Маршал Бирюзов, командовавший в тех боях 132-й стрелковой дивизией, вспоминал: 
        «Куда тяжелей пришлось тогда левому соседу - 137-й стрелковой дивизии, 
        на стыке с которой враг наносил главный удар. Здесь бой достиг наивысшего 
        напряжения. Весь район, казалось, был залит кровью и объят пламенем. Горело 
        все: подожженные немцами деревни, подбитые танки, автомашины…»<br>
        <br>
        В день своего первого боя - 13 июля 1941 года - дивизия отбила все атаки 
        противника и не отступила ни на шаг.<br>
        <br>
        Тогда противник усилил давление на фланги, на соседей справа и слева. 
        К ночи гул танковых моторов и грохот орудий был слышен уже в тылу. Прорвав 
        оборону 13-й армии, танки Гудериана устремились по пыльным дорогам на 
        Пропойск (сейчас Славгород) и Чаусы. Так 137-я стрелковая дивизия оказалась 
        в окружении.<br>
        <br>
        <strong><br>
        Пропавшие без вести</strong><br>
        <br>
        Приказа на отход не было, и дивизия продолжала занимать свои позиции. 
        Стало темнеть, когда наши бойцы захватили немецкую автомашину, заехавшую 
        прямо в боевые порядки. Гитлеровцы то ли сбились с дороги, то ли посчитали, 
        что здесь уже не может быть советских войск. При важных пассажирах оказались 
        ценные документы.<br>
        <br>
        А.В. Шапошников: «В портфеле была и карта с обозначением рубежей, на которых 
        предстояло быть немцам, и даже даты, вплоть до Горького. И, надо сказать, 
        графика движения на восток немцы придерживались.»<br>
        <br>
        Историограф 137-й Бобруйской ордена Суворова стрелковой дивизии Валерий 
        Киселев побывал в июле 1976 года на том месте первого боя у села Червонный 
        Осовец: «... Все заросло бурьяном. На окраине села памятник: бронзовая 
        фигура солдата, ограда, внутри - четыре широких братских могилы. Сколько 
        же здесь лежит наших солдат? Старожилы считают: «Человек двести. Но не 
        все здесь, в лесу много могилок. Как снопов их лежало на поле, как снопов.» 
        Сколько раз мне потом приходилось слышать на длинном пути дивизии от старушек 
        это выражение - «как снопов».<br>
        <br>
        В ночь на 14 июля 137-я стрелковая дивизия получила приказ на отход - 
        четырьмя колоннами в общем направлении на Чаусы.<br>
        <br>
        Гитлеровское командование рассчитывало, что окруженные южнее Чаус советские 
        войска начнут быстро распадаться, и перебросило свои главные силы в район 
        Кричева, где по реке Сож спешно занимали оборону резервные части Красной 
        армии.<br>
        <br>
        Наводчик 45-миллиметрового орудия батареи 409-го стрелкового полка Ф.Е. 
        Петров: «Когда подошли к Кричеву, комбат приказал занять здесь оборону. 
        Наш расчет занял позицию на центральной улице, на правой стороне проезжей 
        части, второе орудие установили на другой улице, так как ждали танки на 
        дороге от станции Чаусы. Прошло несколько минут, начался обстрел, промчалась 
        полуторка, стоявший на подножке незнакомый командир крикнул, что за ним 
        идут немецкие танки. Видел, как снаряды попали в орудия, стоявшие впереди, 
        как повалились там бойцы. Наш командир взвода, увидев это, приказал отступить. 
        Выпустили последний снаряд, и побежали по улице под свист пуль. Нас было 
        трое, забежали во двор, оттуда через огород в овраг. Командира орудия 
        и взводного я больше не видел, что стало со вторым орудием - тоже не знаю.<br>
        <br>
        Прошли сады, в ямках в овраге встретили и подняли еще несколько бойцов. 
        Собралось нас так семь-восемь человек. Солнце закатывалось. Лежим под 
        липой, подошла женщина, расспросили ее об обстановке в городе. Рассказала, 
        что в Кричеве полно немецких автомашин. Рано утром один из нас пошел искать, 
        где бы там, в овраге, попить, и его остановил немец с автоматом. Пришлось 
        и нам подняться. Повел нас всех через двор хозяйки, она еще успела дать 
        нам по кружке молока. Набралось нас человек двадцать, повели к реке, заставили 
        наводить понтонный мост через реку. Сначала нас держали во дворе сельпо, 
        потом перегнали на территорию цементного завода. В начале августа погнали 
        в Могилев. Перед началом движения немцы объявили, что нас здесь пять тысяч 
        человек. Из Кричева до Могилева шли несколько дней. Кто стер ноги и не 
        мог идти, тех немцы пристреливали. В Могилеве нас держали около Дома Красной 
        армии рядом с Днепром. Офицеров, попавших в плен в форме, держали отдельно. 
        Некоторые младшие командиры маскировались под рядовых. После Могилева 
        - Орша, Ново-Борисов, затем Германия. В начале октября нас вывезли на 
        юг Германии, в Шварцвальд. Работали под горой, пробивали тоннель. Здесь 
        меня сильно избили, чудом остался жив. В феврале 42-го, опухшего, меня 
        отправили в лазарет. В мае, после поправочного лагеря, направили на сельхозработы, 
        затем оказался в Лотарингии, на угольных шахтах. Освободили нас 14 апреля 
        45-го американцы…» <br>
		<br>Санинструктор 497-го ГАП В.П. Гаев:<br>
        <br>
        «Перед прорывом из окружения у нас скопилось очень много раненых. Эвакуировать 
        их было невозможно, поэтому разместили всех в лесной деревне Каменка, 
        севернее Варшавского шоссе, в шести километрах от станции Веремейки. С 
        ранеными оставили меня и санинструктора Григория Маличева. Полк пошел 
        на прорыв, а мы... прятались в школе. Помогали нам местные жители всем, 
        чем могли. За три месяца выздоровели и ушли в лес 107 человек. Умер только 
        один политработник. А 14 октября 41-го 23 человека тяжелораненых и нас, 
        медиков, немцы взяли в плен. Сначала был лагерь в Кричеве, на территории 
        цементного завода. Начались ужасы и кошмары. Затем лагерь в Могилеве, 
        а потом 326-й штрафной лагерь в Эльзас-Лотарингии. В декабре 44-го нас 
        освободили американцы…»<br>
        <br>
        ... Дивизия продолжала свой путь на восток: переправилась через реку Проня 
        севернее города Пропойска и 18 июля вышла к Варшавскому шоссе. В ожидании 
        прорывавшихся из окружения остатков 132-й, 137-й и 160-й стрелковых дивизий 
        там уже разместились засады противника.<br>
        <br>
        Помощник начальника тыла 20-го стрелкового корпуса И.И. Цвик:*** «По плану 
        прорыва через шоссе в авангард главных сил ставилась 137-я стрелковая 
        дивизия, справа - 132-я, а слева - 160-я. Меня направили к полковнику 
        Гришину для наблюдения и помощи. Персонально Гришин смотрелся лучше других 
        командиров дивизий. Видно было, что этот человек - с железной волей...»<br>
        <br>
        А.В. Шапошников:<br>
        <br>
        «В этот момент к нам подъехала машина командира корпуса. Генерал-майор 
        Еремин приехал лично проверить исполнение плана прорыва. Был он весь запыленный, 
        небритый, смертельно уставший, так что его трудно было узнать. Я доложил 
        обстановку. Он: «Надо оседлать шоссе, пробить брешь до Сожа и занять оборону 
        на том берегу реки. Выполните - молодец, не выполните - расстреляю.» Вздохнул 
        и уехал. Больше я его никогда не видел.»<br>
        <br>
        К реке Сож вышли весь 771-й стрелковый полк, часть 278-го ЛАП, батальон 
        409-го стрелкового полка, управление дивизии во главе с полковником Гришиным. 
        Другим подразделениям перейти Варшавского шоссе не удалось. Погибли командир 
        20-го стрелкового корпуса генерал-майор Сергей Илларионович Еремин и многие 
        штабные работники.<br>
        <br>
        Командир автобронетанковой роты разведбата 137-й стрелковой дивизии В.Г. 
        Бакиновский: «Перед войной наш батальон был серьезной силой: около пятисот 
        человек, двадцать мотоциклов, десять бронеавтомобилей, рота плавающих 
        танков. Первую неделю войны разведкой практически не занимались, не имея 
        связи с начальником разведки майором Зайцевым. Через Варшавское шоссе 
        мы переходили первыми, полков еще не было. Поехали туда на легковой машине, 
        попали под пулеметный огонь, машину со всем экипажем расстреляли, пришлось 
        возвращаться одному. Вернулся в батальон - там все горит: машины, танки. 
        «В чем дело?» - спрашиваю. «Комбат Соломин приказал все уничтожить и уходить». 
        Сам он из окружения не вышел. Холеный офицер, был кавалеристом, в технике 
        ничего не понимал. Да и коня своего не любил: напинает сначала, а потом 
        садится.<br>
        <br>
        Командир 238-го ОИПТД майор Маков тоже исчез, но говорили, что, когда 
        по шоссе шла немецкая колонна, он выскочил и сел в их танк. После прорыва 
        из окружения наш разведбат расформировали, а меня перевели в 771-й полк.»<br>
        <br>
        Командир батальона связи 137-й стрелковой дивизии Ф.М. Лукьянюк: «Комиссара 
        и начальника штаба разведбата решением трибунала разжаловали в рядовые. 
        Это были не единственные случаи трусости и предательства. Тогда в дивизии 
        шли разговоры, что изменниками оказались начальник артиллерии дивизии, 
        замначальника политотдела по комсомолу. Много было претензий у командира 
        дивизии к разведке. Ее начальник майор Зайцев в мирное время показал себя 
        как грамотный и подготовленный командир, но на фронте - как трус и паникер. 
        По разведке он ни одного приказа полковника Гришина не выполнил, мало 
        того, своим враньем вводил его в заблуждение. Зайцева судил трибунал, 
        он на коленях ползал, просил прощенья.»<br>
        <br>
        Когда мы переправились через Сож, то выяснилось, что штаб нашего корпуса 
        почти полностью уничтожен. Дивизия осталась без вышестоящего руководства. 
        Тогда поехали в ближний сельсовет и добились прямой связи с Москвой. Полковник 
        Гришин говорил, насколько я знаю, с кем-то из своих товарищей в Генеральном 
        штабе, доложил обстановку и получил приказ совместно с воздушно-десантным 
        корпусом отбить у немцев Пропойск. Мне поручили найти командира этого 
        корпуса и договориться с ним о совместных действиях. Когда я его нашел, 
        то он мне сказал: «Ничем вам помочь не могу, у меня ничего нет, кроме 
        людей…»<br>
        <br>
        Прикрывая переправу частей дивизии через Сож, погиб 2-й батальон 771-го 
        стрелкового полка.<br>
        <br>
        А.В. Шапошников:<br>
        <br>
        «Все последующие дни, как только ветер дул со стороны немцев, становилось 
        невозможно дышать от смрада. Трупы никто не убирал, а стояла жара.<br>
        <br>
