        репрессий».<br>
        <br>
        Ф.М. Лукьянюк припомнил еще один весьма странный случай: «Весной 1941-го 
        дивизия перешла на новые штаты, которые считались секретными. И вдруг, 
        эти документы после одного из совещаний в штабе дивизии пропали. А нашли 
