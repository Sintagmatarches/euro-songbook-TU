        <br>
        После загадочного исчезновения командира 771-го стрелкового полка лишились 
        своих должностей начальники особых отделов НКВД и дивизии, и полка Горшков 
        и Потехин как «не обеспечившие оперативный надзор в закрепленных за ними 
        частях».<br>
        <br>
