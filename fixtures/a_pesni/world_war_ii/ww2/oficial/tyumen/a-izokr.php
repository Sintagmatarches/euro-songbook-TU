        сил прорыва. Имена тюменцев 1919-1921 годов рождения, призванных в 137-ю 
        стрелковую дивизию до войны и переживших немецкий плен можно найти в книге 
        «Запрещенные солдаты». Все они также считались пропавшими без вести.<br>
        <br>
        Пробившиеся из окружения части 137-й получили пополнение из мобилизованных 
        на войну тюменцев, родившихся с 1885 по 1918 годы. Тогда они еще не знали, 
        что попадут в другое окружение. Более масштабное и жестокое.<br>
