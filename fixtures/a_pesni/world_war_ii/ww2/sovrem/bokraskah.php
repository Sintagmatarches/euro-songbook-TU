Музыка Оскара Фельцмана<br>
Слова Роберта Рождественского<br>
<br>
Был он рыжим, как из рыжиков рагу,<br>
  Рыжим, словно апельсины на снегу.<br>
  Мать шутила, мать веселою была:<br>
  «Я от солнышка сыночка родила!»<br>
  А другой был черным-черным у нее,<br>
  Черным, будто обгоревшее смолье.<br>
  Хохотала над расспросами она,<br>
  Говорила: «Слишком ночь была черна!»<br>
<br>
В сорок первом, в сорок памятном году,<br>
  Прокричали репродукторы беду.<br>
  Оба сына, оба двое, соль земли –<br>
  Поклонились маме в пояс. И ушли.<br>
  Довелось в бою почуять молодым<br>
  Рыжий бешеный огонь и черный дым,<br>
  Злую зелень застоявшихся полей,<br>
  Серый цвет прифронтовых госпиталей.<br>
<br>
Оба сына, оба двое, два крыла<br>
  Воевали до победы. Мать ждала.<br>
  Не гневила, не кляла она судьбу.<br>
  Похоронка обошла ее избу.<br>
  Повезло ей, привалило счастье вдруг.<br>
  Повезло одной на три села вокруг.<br>
  Повезло ей. Повезло ей! Повезло!<br>
  Оба сына воротилися в село.<br>
<br>
Оба сына, оба двое, плоть и стать.<br>
  Золотистых орденов не сосчитать.<br>
  Сыновья сидят рядком – к плечу плечо.<br>
  Руки целы, ноги целы – что еще!<br>
  Пьют зеленое вино, как повелось.<br>
  У обоих изменился цвет волос –<br>
  Стали волосы смертельной белизны…<br>
  Видно, много белой краски у войны.<br>
