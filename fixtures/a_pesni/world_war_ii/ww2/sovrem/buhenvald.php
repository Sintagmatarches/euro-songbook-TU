Летом 1958 года писатель-фронтовик Александр Соболев с женой Татьяной отдыхал в городе Озеры Московской области. По радио сообщили об открытии на месте концлагеря &quot;Бухенвальд&quot; мемориала, деньги на который собрали жители  ГДР. Мемориал выполнен в виде башни с колоколом, бьющим в набат. Соболев вдохновился, написал стихотворение и отнес в &quot;Правду&quot;. Там не напечатали, но напечатали в &quot;Труде&quot;. <br>
<br>
Писатель послал стихи композитору Вано Мурадели, тот положил текст музыку. Затем Соболев пришел с песней в ЦК ВЛКСМ, там она понравилась и была отправлена в Вену на Всемирный фестиваль молодежи и студентов, где прозвучала в исполнении хора студентов Уральского университета. Затем вошла в документальный фильм &quot;Весенний ветер над Веной и в репертуар ансамбля Александрова. <br>
<br>
Вдова Соболева утверждала, что имя Соболева при исполнении и публикации песни не упоминалось, гонораров он за нее не получал, а жили они с женой в бараке. Умер он в 1986 году, в первая его книга вышла только через десять лет. <br>
