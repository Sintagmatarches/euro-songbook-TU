Музыка Александры Пахмутовой<br>
  Слова Михаила Львова<br>
<br>
Клубились яростно метели<br>
  По сталинградской по земле.<br>
  Дымились потные шинели,<br>
  И шли солдаты по золе.<br>
  Машинам жарко и пехоте,<br>
  И наше сердце не в броне.<br>
  Снежинки таяли в полете,<br>
  Как ветки с листьями в огне.<br>
<br>
И падал в битве человек<br>
  В горячий снег, в кровавый снег.<br>
<br>
Смертельной битвы этой ветер,<br>
  Как бы расплавленный металл,<br>
  Сжигал и плавил все на свете,<br>
  Что даже снег горячим стал.<br>
  И за чертой, последней, страшной,<br>
  Бывало, танк и человек<br>
  Встречались в схватке рукопашной,<br>
  И превращался в пепел снег.<br>
<br>
Хватал руками человек<br>
  Горячий снег, кровавый снег…<br>
<br>
Опали белые метели.<br>
  Цветами стали по весне.<br>
  Большие годы пролетели,<br>
  А ты всем сердцем – на войне,<br>
  Где отпевали нас метели,<br>
  Где в землю лучшие легли.<br>
  …А дома – мамы поседели.<br>
  У дома – вишни зацвели.<br>
<br>
А у тебя в глазах навек –<br>
  Горячий снег, кровавый снег.<br>
