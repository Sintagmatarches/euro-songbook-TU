Припев:<br>
<br>
Пусть время проходит,<br>
Пусть годы летят,<br>
Друзей фронтовых не забудет солдат.<br>
Нет, нет, нет, нет,<br>
Нет, не забудет солдат.<br>
Нет, нет, нет, нет,<br>
Нет, не забудет солдат.<br>
<br>
Судьба разбросала по свету друзей,<br>
Они не плясали на свадьбе моей,<br>
Но первый налитый до края бокал<br>
Я поднял за старых друзей и сказал:<br>
<br>
Припев.<br>
<br>
Я память о друге своем берегу –<br>
Он пал на далеком чужом берегу,<br>
Он нашей победы не встретил зарю,<br>
И сыну я имя его подарю.<br>
<br>
Припев.<br>
<br>
Но если настанет такая пора,<br>
Что дымом и гарью запахнут ветра,<br>
То сколько нам в жизни положено жить,<br>
Как в годы былые, мы будем дружить.<br>
<br>
Припев.<br>
