Музыка Никиты Богословского<br>
  Слова Евгения Евтушенко<br>
<br>
Давным-давно прошла война,<br>
  Но у Мамаева кургана<br>
  Сейчас такая тишина,<br>
  Что слышны шорохи тумана.<br>
<br>
И только запах чабреца,<br>
  И только ястреб онемелый,<br>
  И только слышатся сердца<br>
  В груди гранитной монументов.<br>
<br>
И по-девчоночьи хрупка,<br>
  Проста, как сердца чистый отзвук,<br>
  Есть в книге записей строка,<br>
  Так непохожая на отзыв.<br>
<br>
И, вспоминая ту войну,<br>
  Читают люди на кургане:<br>
  «Спасибо вам за тишину», - <br>
  И подпись тоненькую: «Таня».<br>
<br>
Ах, набинтуй, туман, бинтов!..<br>
  Кто эта Таня – я не знаю,<br>
  Но все мы дети тех бойцов,<br>
  Кого взяла земля сырая.<br>
<br>
Мы будем свято помнить их,<br>
  Здесь прикипавших к пулеметам…<br>
  Так надо жить, чтоб за живых<br>
  В земле не стало стыдно мертвым.<br>
<br>
Я счастлив тем, что я родной<br>
  Любому майскому побегу,<br>
  Что светит солнце над страной<br>
  Медалью общей за победу.<br>
<br>
И, вечно помня ту войну,<br>
  Читаю вечно на кургане:<br>
  «Спасибо вам за тишину», - <br>
  И подпись тоненькую: «Таня».<br>
