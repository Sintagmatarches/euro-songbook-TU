<h1>ТУМАН</h1>

Из кинофильма «Хроника пикирующего бомбардировщика» (1967)<br>
<br>
Музыка Александра Колкера<br>
Слова Кима Рыжова<br>
<br>
Туман, туман,<br>
Седая пелена...<br>
И всего в двух шагах<br>
За туманами война.<br>
И гремят бои без нас,<br>
Но за нами нет вины,<br>
Мы к земле прикованы туманом,<br>
Воздушные рабочие войны.<br>
<br>
Туман, туман,<br>
На прошлом, на былом...<br>
Далеко-далеко<br>
За туманами наш дом.<br>
А в землянке фронтовой<br>
Нам о доме снятся сны.<br>
Видно, все мы рано повзрослели,<br>
Воздушные рабочие войны.<br>
<br>
Туман, туман,<br>
Окутал землю вновь...<br>
Далеко-далеко<br>
За туманами любовь.<br>
Долго нас невестам ждать<br>
С чужедальней стороны,<br>
Мы не все вернемся из полета -<br>
Воздушные рабочие войны.<br>
<br>
Туман, туман...<br>
