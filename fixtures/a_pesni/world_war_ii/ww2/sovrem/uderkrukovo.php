<br><br>
  В ноябре-декабре 1941 года в районе деревни Крюково проходил рубеж обороны Москвы. 
  В 1960-63 годах на этом месте был возведен город Зеленоград (окрестные леса сохранились).  <br>
<br>
Текст песни основан на стихотворении  «Баллада о верности», опубликованном Сергеем Островым в 1971 году. В первоначальном варианте было девять строф. Часть текста Островой убрал, часть изменил, добавил новые строки.

Марк Фрадкин отдал готовую песню популярному тогда ансамблю «Самоцветы». Однако вскоре, в конце 1975 года из-за разногласий с руководителем ансамбля Юрием Маликовым почти все музыканты из него ушли и основали ВИА «Пламя», который и записал «У деревни Крюково». В этом исполнении песня прозвучала в концерте «Песня-75», солировал  Валентин Дьяконов.<br>
