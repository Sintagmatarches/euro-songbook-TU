function normalizeWithAliases(value, canonicalSet, aliases) {
  const raw = clean(value);
  if (!raw) return null;
  if (canonicalSet.has(raw)) return raw;
  const alias = aliases[fold(raw)];
  return alias && canonicalSet.has(alias) ? alias : null;
}

export function normalizeSongLanguage(value) {
  return normalizeWithAliases(value, LANGUAGE_SET, LANGUAGE_ALIASES);
}

export function normalizeSongCountry(value) {
  return normalizeWithAliases(value, COUNTRY_SET, COUNTRY_ALIASES);
}

export function normalizeSongPeriod(value) {
  return normalizeWithAliases(value, PERIOD_SET, PERIOD_ALIASES);
}

export function getPeriodMeta(value) {
  const canonical = normalizeSongPeriod(value);
  return canonical ? (PERIOD_META_BY_VALUE.get(canonical) || null) : null;
}

export function getPeriodValuesForCountry(country) {
  const canonicalCountry = normalizeSongCountry(country || "");
  if (!canonicalCountry) return [];
  return PERIOD_DEFINITIONS
    .filter((definition) => Array.isArray(definition.countries) && definition.countries.includes(canonicalCountry))
    .map((definition) => definition.value);
}

export function countrySupportsPeriods(country) {
  return getPeriodValuesForCountry(country).length > 0;
}

export function isPeriodAllowedForCountry(country, period) {
  const canonicalCountry = normalizeSongCountry(country || "");
  const canonicalPeriod = normalizeSongPeriod(period || "");
  if (!canonicalCountry || !canonicalPeriod) return false;
  return getPeriodValuesForCountry(canonicalCountry).includes(canonicalPeriod);
}

export function normalizeSongCatalogInput(input = {}, options = {}) {
  const allowEmptyLang = options.allowEmptyLang === true;
  const rawLang = clean(input.lang);
  const lang = rawLang ? normalizeSongLanguage(rawLang) : null;
  if (rawLang && !lang) return { ok: false, error: "Invalid language value" };
  if (!lang && !allowEmptyLang) return { ok: false, error: "Invalid language value" };

  const rawCountry = clean(input.country);
  const country = rawCountry ? normalizeSongCountry(rawCountry) : null;
  if (rawCountry && !country) return { ok: false, error: "Invalid country value" };

  const rawPeriod = clean(input.period);
  if (!rawPeriod) return { ok: true, value: { lang, country, period: null } };
  const period = normalizeSongPeriod(rawPeriod);
  if (!period) return { ok: false, error: "Invalid period value" };
  if (!countrySupportsPeriods(country)) return { ok: false, error: "Period is not supported for selected country" };
  if (!isPeriodAllowedForCountry(country, period)) return { ok: false, error: "Invalid period for selected country" };
  return { ok: true, value: { lang, country, period } };
}

function labelFor(kind, value, locale) {
  const lc = pickLocale(locale);
  if (kind === "country") {
    const simple = CURRENT_COUNTRY_SIMPLE_LABELS[lc]?.[value] || CURRENT_COUNTRY_SIMPLE_LABELS.en?.[value] || "";
    if (simple) return simple;
  }
  const table = LABELS[kind];
  if (lc === "uk" || lc === "et") {
    return table?.[lc]?.[value] || table?.en?.[value] || table?.ru?.[value] || value;
  }
  return table?.[lc]?.[value] || table?.en?.[value] || table?.ru?.[value] || value;
}

function compactHistoricalRangeLabel(label, locale) {
  const safeLabel = clean(label);
  if (!safeLabel) return safeLabel;
  if (pickLocale(locale) === "ru") {
    return safeLabel.replace(/наше время/gi, "н.в.");
  }
  return safeLabel;
}

export function getCatalogLabel(kind, value, locale) {
  const raw = clean(value);
  if (!raw) return "-";
  if (kind === "language") {
    const canonical = normalizeSongLanguage(raw);
    return canonical ? compactHistoricalRangeLabel(labelFor("language", canonical, locale), locale) : raw;
  }
  if (kind === "country") {
    const canonical = normalizeSongCountry(raw);
    return canonical ? compactHistoricalRangeLabel(labelFor("country", canonical, locale), locale) : raw;
  }
  if (kind === "period") {
    const canonical = normalizeSongPeriod(raw);
    return canonical ? compactHistoricalRangeLabel(labelFor("period", canonical, locale), locale) : raw;
  }
  return raw;
}

function getKindValues(kind, options = {}) {
  if (kind === "language") return SONG_LANGUAGE_VALUES;
  if (kind === "country") return SONG_COUNTRY_VALUES;
  if (kind === "period") {
    const filtered = getPeriodValuesForCountry(options.country || "");
    return filtered.length ? filtered : SONG_PERIOD_VALUES;
  }
  return [];
}

function normalizeForKind(kind, value) {
  if (kind === "language") return normalizeSongLanguage(value);
  if (kind === "country") return normalizeSongCountry(value);
  if (kind === "period") return normalizeSongPeriod(value);
  return null;
}

export function getCatalogOptions(kind, locale, currentValue = "", config = {}) {
  const values = getKindValues(kind, config);
  const items = values.map((value) => ({
    value,
    label: getCatalogLabel(kind, value, locale),
    legacy: false,
  }));
  if (kind === "country") {
    const collator = new Intl.Collator(pickLocale(locale), { sensitivity: "base", numeric: true });
    items.sort((a, b) => collator.compare(a.label, b.label));
  }

  const raw = clean(currentValue);
  if (!raw) return items;
  const canonical = normalizeForKind(kind, raw);
  if (canonical) return items;

  items.unshift({
    value: raw,
    label: `${raw} (${getUiText("legacyValue", locale)})`,
    legacy: true,
  });
  return items;
}

export function getUiText(key, locale) {
  const lc = pickLocale(locale);
  return FIELD_LABELS[key]?.[lc] || FIELD_LABELS[key]?.en || key;
}

