<br>
Вероника Долина<br>
<br>
А пока я снимала, как Люк 
  Бессон,<br>
  Молодых людей на Майорке,<br>
  Мой волшебник-дед постучал в мой сон,<br>
  Весь веснушчатый, в гимнастерке.<br>
  - Подымайся, бессовестная, - сказал, -<br>
  Ты не Шурочка в пелеринке.<br>
  Видишь: банк, телеграф, наконец, вокзал<br>
  Аннексируем по старинке.<br>
<br>
Не затем я шашкой махал 
  в степи<br>
  И в Одессу входил с Котовским,<br>
  Чтобы ты писала одни стихи,<br>
  Хоть и с выговором московским.<br>
  Не затем окруженье, Якир, мятеж<br>
  Или Питер, зима, блокада,<br>
  И семь раз отмерь, а семью отрежь,<br>
  И смотри, отрежь, кого надо.<br>
<br>
Много сказок при свете полной 
  луны<br>
  Я б тебе рассказал, босячке,<br>
  Чтобы тонны слюны в масштабах страны<br>
  Дали павловские собачки.<br>
  Подымайся, деточка, труд не велик,<br>
  Я устал говорить и сниться,<br>
  Пусть мой старший правнук духи «Лалик»<br>
  Сам привозит тебе из Ниццы.<br>
<br>
Твоих глупых сверстников 
  Карабас<br>
  Заставляет бежать по нитке.<br>
  Полюбуйся: твои Гайдар, Чубайс – <br>
  Оголтелые пережитки.<br>
  Дунь налево, направо – твой брат Шекспир,<br>
  Твой учитель, возможно, Лютер,<br>
  И не тронет тебя ни один вампир,<br>
  Когда сядешь ты за компьютер.<br>
