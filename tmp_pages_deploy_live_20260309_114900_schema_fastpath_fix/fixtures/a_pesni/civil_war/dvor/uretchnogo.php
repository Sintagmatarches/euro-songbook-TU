      <br>
      У речного далекого брода,<br>
      Где, родная, прощался с тобой,<br>
      Ты сказала, до Нового года<br>
      Не вернешься, наверно, домой.<br>
<br>
На прощанье мне руку 
      пожала,<br>
      Посмотрела с улыбкой в глаза.<br>
      Мне на шею платок повязала,<br>
      И по щеке покатилась слеза.<br>
<br>
Я ушел по тропинке унылой,<br>
      Где б я ни был и где ни бывал,<br>
      Но платочек, повязанный милой,<br>
      У горячего сердца держал.<br>
<br>
Прогремела война по 
      дорогам,<br>
      Прогремел революции гром,<br>
      Возвращался тропинкой знакомой<br>
      Я в карельский-то брошенный дом.<br>
<br>
Друга детства тропинкой 
      я встретил<br>
      И спросил о любимой своей.<br>
      Поглядев, он мне тихо ответил,<br>
      Что теперь не увидишься с ней.<br>
<br>
Белофинны об этой узнали,<br>
      Что ушел с партизанами я.<br>
      И у брода ее расстреляли,<br>
      Все за то, что любила меня.<br>
<br>
Если грянет войны непогода,<br>
      Я пойду за страну воевать.<br>
      На могиле у дальнего брода<br>
      Мы простимся с тобою опять. <br>
	  <br>
      <em>Неизвестный источник </em><br>
<br>
<br>
      Вероятно, песня отражает события 1918-1922 годов - гражданскую войну между &quot;белыми&quot; и &quot;красными&quot; в Финляндии, победу там &quot;белых&quot; и последующие войны  Финляндии с советской Россией, из-за которых перекраивалась граница. В этих конфликтах обе стороны поощряли партизан, действующих на территории противника. Дошло и до прямых агрессий. В марте 1918 года отряды финской армии вошли в карельские земли Архангельской губернии, после чего местными сепаратистами было провозглашено  Северокарельское государство, признанное Финляндией. В мае 1920 года РККА его ликвидировала, заняв столичное село Ухта.  В ноябре-декабре 1921 года финны вновь вошли в район, но к началу января были разбиты силами РККА, которая в феврале 1922-го вновь вошла в Ухту. На этом история Северокарельского государства закончилась. Тем не менее, в ходе конфликтов 1918-1922 годов к Финляндии отошла Печенгская область (Петсамо) в Заполярье, западная часть полуострова Рыбачий и большая часть полуострова Среднего.<br>
	  <br>
	  Но песня вполне могла появиться и  в 1939 году во время новых осложнений с Финляндией, вылившихся в войну 1939-40 годов. Термин &quot;белофинн&quot; советская пропаганда начала активно использовать в 1939 году, накануне агрессии на Финляндию, не согласившуюся вновь пересмотреть границу по просьбе СССР -  отодвинуть ее от Ленинграда, уступить несколько островов и отдать в аренду полуостров Ханко в обмен на уступку части Карелии. Советский Союз намеревался таким образом укрепить северные границы на случай войны с Германией.<br>
