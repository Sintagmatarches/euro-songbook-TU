На просторе Каспий-моря...</strong><br>
<br>
На просторе Каспий-моря <br>
По искрящейся волне <br>
Пароход плывёт военный <br>
С белым флагом на корме.<br>
<br>
Наверху - штаб офицеров<br>
И полковник в орденах.<br>
На ногах сверкают шпоры <br>
И погоны на плечах.<br>
<br>
Пьют вино, кричат, смеются: <br>
- На верёвку вздёрнем всех!<br>
И Совет разгоним лихо,<br>
Без препятствий и помех.<br>
<br>
Вновь застонет люд рабочий<br>
Под нагайкой палачей,<br>
И польютс яу застенков<br>
Слёзы, крови горячей!<br>
<br>
У котла у парового, <br>
Чёрен, грязен и угрюм, <br>
Кочегар сидит, согнувшись <br>
Под наплывом тяжких дум.<br>
<br>
Вдруг он слышит в рупор голос: <br>
- Город виден. Тихий ход!<br>
Ты прощай, жена и дети, <br>
И прощай, людской ты род!<br>
<br>
Быстро клапан закрывает.<br>
Уголь в топку весь свалил<br>
И на палубу взбегает<br>
Изо всех последних сил.<br>
<br>
- Подойди! - несётся грозно.<br>
Пар взрывается столбом.<br>
Пароход взлетел на воздух, <br>
Тонет в море голубом.<br>
<br>
Волны в пене. Солнце блещет.<br>
На просторе — никого. <br>
Власть Советская дороже <br>
Бела света самого!<br>
