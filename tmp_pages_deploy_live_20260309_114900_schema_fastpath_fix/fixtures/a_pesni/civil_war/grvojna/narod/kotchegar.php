	<span class="header"><strong>КОЧЕГАР</strong> </span>
<br>
<br>
На просторе Каспий-моря <br>
По искрящейся волне <br>
Пароход идет военный <br>
С флагом белым на корме.<br>
<br>
На верху штаб-офицеры <br>
И полковник в орденах – <br>
На ногах сверкают шпоры <br>
И погоны на плечах.<br>
<br>
Пьют вино, кричат, смеются: <br>
«На веревку вздернем всех <br>
И Совет разгоним лихо <br>
Без препятствий и помех».<br>
<br>
У котла у парового, <br>
Черен грязью и угрюм, <br>
Кочегар сидит согнувшись <br>
От наплыва тяжких дум:<br>
<br>
«Скоро будет Александровск, <br>
Там не ждут таких гостей; <br>
Власть Советская лишится <br>
Снова лучших сыновей».<br>
<br>
Вдруг он слышит в рупор голос: <br>
«Город видишь, тихий ход». <br>
«Эй, прощай, жена и дети, <br>
И прощай, честной народ!»<br>
<br>
Быстро клапан закрывает, <br>
В топку сыпет уголь весь <br>
И на палубу взбегает, <br>
Словно грозной смерти весть.<br>
<br>
Вслед за ним несется грохот, <br>
Пар вздымается столбом, <br>
Пароход взлетел на воздух, <br>
Тонет в море голубом.<br>
<br>
Море в пене, солнце блещет, <br>
На просторе — никого. <br>
Власть Советская дороже <br>
Бела света самого.<br>
