 <br>
  <br>
  Автор текста - комсомолец Николай Кооль. <br>
  <br>
        Красноармейская переделка казачьей песни времен русско-японской войны 
        <a href="../../kazaki/za_laohe.php">&quot;За рекой Ляохэ загорались огни&quot;</a> 
        о неудачном штурме казаками города Инкоу в декабре 1904 года (не все признают подлинность &quot;За рекой Ляохэ&quot;, некоторые считают ее 
        современной подделкой; в этом случае, Кооль опирался на другие песни на 
        этот мотив). На эту мелодию ранее пелась каторжанская песня <a href="../../popular20/kogda_na.htm">&quot;Лишь 
        только в Сибири займется заря&quot;</a> (2-я половина XIX века) и, предположительно, 
        цыганский романс <a href="../../romans/andaluz.php">&quot;Андалузянка&quot;</a> 
        на стихотворение Всеволода Крестовского, написанное в 1862 году. В &quot;Андалузянке&quot; 
        есть текстовая параллель с &quot;Там вдали, за рекой&quot;:<em> &quot;Расскажу 
        я ему, как была эта ночь Горяча, как луна загоралась&quot;. <br>
  </em><br>
        В интернете встречается аналогичный красноармейскому <a href="../bel-dvor/tamvdali-bel.php">&quot;деникинский&quot;</a> 
        вариант, созданный уже в наши дни. В 2012 году от потомка оренбургских казаков записан <a href="../bel/tamvdali-dut.php">дутовский</a> вариант, тоже сомнительный. Но не исключено, что в годы Гражданской 
        войны какие-то красные и белые песни на этот мотив все же существовали. 
        <br>
  <br>
        Записано много фольклорных вариантов песни Кооля. Ее популярность вызвала 
        появление пародий: &quot;Там вдали, у реки собирались братки...&quot;, 
        песня про израильскую армию (&quot;И бесплатно отряд поскакал на врага, 
        Завязалась кровавая драка. И &lt;?&gt; молодой вдруг поник головой, Рабиновича 
        ранили в сраку. Он упал между ног вороного коня И закрыл свои карие очи...&quot;) 
        и т.д.<br>
  <br>
        Можно импровизировать и вставлять в песню любых подходящих героев:<br>
