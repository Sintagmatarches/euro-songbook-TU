        <strong>Описание</strong>. Изображен доброволец в форме корниловского 
        ударного полка. Черно-красная корниловская фуражка с красной тульей и 
        черным околышем. На тулье бело-сине-красная кокарда (принятая в Добровольческой 
        армии вместо &quot;адамовой головы&quot;). Так же на изображенном видны 
        два шеврона: бело-сине-красный добровольческий и &quot;ударный&quot; корниловский 
        (принят в бытность полка 1-м ударным отрядом 8-й армии Юго-Западного фронта). 
        Кроме того на рисунке видно корниловскую эмблему - &quot;адамова голова&quot; 
        со скрещенными под ней мечами (над черепом должна была быть надпись &quot;корниловцы&quot;, 
        на рисунке ее нет). Погоны корниловские черно-красные. <br>
