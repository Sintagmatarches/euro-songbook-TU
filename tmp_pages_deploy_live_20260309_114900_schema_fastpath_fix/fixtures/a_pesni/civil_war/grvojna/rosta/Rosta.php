          <br>
          <span class="header"><strong>ТЕКСТЫ МАЯКОВСКОГО К ОКНАМ РОСТА<br>
          </strong></span> <br>
          <br>
          <img src="foto-rosta.jpg" width="315" height="387"><br>
          <em>Владимир Маяковский, Иван Малютин, Михаил Черемных - художники РОСТА,
          1919</em><br>
        </div>
        <br>
        <br>
        Плакаты "Окна РОСТА" выпускались с сентября 1919 года по январь 1921 года, 
        затем до января 1922 года выпуск агитплакатов продолжался Главполитпросветом. 
        Ниже дана основная масса текстов Маяковского к &quot;окнам&quot;. Приводится по изд.: Владимир Маяковский. Собр. соч. в 6 т. Т. 5. М., Библиотека &quot;Огонек&quot;, Правда, 1973.<br>
        <br>
        <br>
        <div align="center"><strong>ПРОШУ СЛОВА... <br>
          (Предисловие Маяковского к сборнику &quot;Грозный смех. Окна РОСТА&quot;. 
          Сост. в 1929 г., вышел в 1932 г.)</strong><br>
        </div>
        <br>
        Это — не только стихи.<br>
        <br>
        Эти иллюстрации — не для графических украшений. <br>
        <br>
        Это — протокольная запись труднейшего трехлетия революционной борьбы, 
        переданная пятнами красок и звоном лозунгов. <br>
        <br>
        Это — моя часть огромнейшей агитработы — окон сатиры РОСТА. <br>
        <br>
        Пусть вспоминают лирики стишки, под которые влюблялись. Мы рады вспомнить 
        и строки, под которые Деникин бежал от Орла. <br>
        <br>
        Любителям высокотарифных описаний задним числом романтики гражданской 
        войны в стиле «констрюктивист» неплохо поучиться на действительном материале 
        боевых лет, на действительной словесной работе этого времени. <br>
        <br>
        Есть такие новые русские древние греки, которые все умеют засахарить и 
        заэстетизировать. <br>
        <br>
        Вот В. Полонский в книге о революционном плакате, вырвав из середины кусок, 
        набредя на агитсатиру РОСТА времен боев с панами, агитку, весь смысл которой 
        доказать: <br>
        <br>
        Так кормите ж красных рать, <br>
        хлеб неси без вою, <br>
        чтобы хлеб не потерять <br>
        вместе с головою, — <br>
        <br>
        этот самый Полонский вырывает из агитки случайный клочок и пишет «фрагмент». 
        Не угодно ли?!<br>
        <br>
        Так же может поступить историк литературы, приводящий слово «соединяйтесь» 
        с подписью «фрагмент», чтоб все догадывались и радовались, что сие — «фрагмент» 
        лозунга «Пролетарии всех стран, соединяйтесь!» <br>
        <br>
        Полонский не только не старается понять и систематизировать цель и направленность 
        плакатных ударов, но просто вдохновенно парит над низменностью агитационного 
        текста. Сейчас, с десятилетием ростинской работы, Третьяковская галерея, 
        газеты, журналы любопытно и восторженно подбирают, клеят и смотрят клочки 
        вручную крашенных листов, этих предков всех многотысячных сегодняшних 
        сатирических журналов. Первые окна сатиры делались в одном экземпляре 
        и вывешивались в немедленно обступаемых народом витринах и окнах пустующих 
        магазинов, дальнейшие размножались трафаретом, иногда до ста — ста пятидесяти 
        экземпляров, расходившихся по окнам агитпунктов. <br>
        <br>
        Всего около девятисот названий по одной Москве. Ленинград, Баку, Саратов 
        стали заводить свои окна. <br>
        <br>
        Диапазон тем огромен: <br>
        <br>
        Агитация за Коминтерн и за сбор грибов для голодающих, борьба с Врангелем 
        и с тифозной вошью, плакаты о сохранении старых газет и об электрификации 
        Я рылся в Третьяковке, в Музее революции, в архивах участников. Едва ли 
        от всей массы окон осталось сейчас более ста целых листов. Мы работали 
        без установки на историю и славу. Вчерашний плакат безжалостно топтался 
        в десятках переездов. Надо сохранить и напечатать оставшиеся, пока не 
        поздно. Только случайно найденный у М. Черемных альбом фотографий дал 
        возможность разыскать тексты и снимки с исчезнувшего. <br>
        <br>
        Моя работа в РОСТА началась так: я увидел на углу Кузнецкого и Петровки, 
        где теперь Моссельпром, первый вывешенный двухметровый плакат. Немедленно 
        обратился к заву РОСТОЙ, тов. Керженцеву, который свел меня с М. М. Черемных 
        — одним из лучших работников этого дела. <br>
        <br>
        Второе окно мы делали вместе. Дальше пришел Малютин, а потом художники: 
        Лавинский, Левин, Брик, Моор, Нюренберг и другие, трафаретчики: Шиман, 
        Михайлов, Кушнер и многие еще, фотограф Никитин. Первое время над текстом 
        работал тов. Грамен, дальше почти все темы и тексты мои; работали еще 
        над текстом О. Брик, Р. Райт, Вольпин. В двух случаях, отмеченных в книге 
        звездочками, я нетвердо помню свое авторство текста. Сейчас, просматривая 
        фотоальбом, я нашел около четырехсот одних своих окон. В окне от четырех 
        до двенадцати отдельных плакатов, значит в среднем этих самых плакатов 
        не менее трех тысяч двухсот. <br>
        <br>
        Подписей — второе собрание сочинений. (В этой книге — малая часть.) <br>
        <br>
        Как можно было столько сделать? <br>
        <br>
        Вспоминаю — отдыхов не было. Работали в огромной нетопленной, сводящей 
        морозом (впоследствии — выедающая глаза дымом буржуйка) мастерской РОСТА. 
        Придя домой, рисовал опять, а в случае особой срочности клал под голову, 
        ложась спать, полено вместо подушки с тем расчетом, что на полене особенно 
        не заспишься и, поспав ровно столько, сколько необходимо, вскочишь работать 
        снова. <br>
        <br>
        С течением времени мы до того изощрили руку, что могли рисовать сложный 
        рабочий силуэт от пятки с закрытыми глазами, и линия, обрисовав, сливалась 
        с линией. <br>
        <br>
        По часам Сухаревки, видневшимся из окна, мы вдруг роем бросались на бумагу, 
        состязались в быстроте наброска, вызывая удивление Джона Рида, Голичера 
        и других заезжих, осматривающих нас иностранных товарищей и путешественников. 
        От нас требовалась машинная быстрота: бывало, телеграфное известие о фронтовой 
        победе через сорок минут — час уже висело по улице красочным плакатом. 
        <br>
        <br>
        «Красочным» — сказано чересчур шикарно, красок почти не было, брали любую, 
        чуть размешивая на слюне. Того темпа, этой быстроты требовал характер 
        работы, и от этой быстроты вывешивания вестей об опасности и о победе 
        зависело количество новых бойцов. И эта часть обшей агитации подымала 
        на фронт. <br>
        <br>
        Вне телеграфной, пулеметной быстроты — этой работы быть не могло. Но мы 
        делали ее не только в полную силу и серьезность наших умений, но и революционизировали 
        вкус, подымали квалификацию плакатного искусства, искусства агитации. 
        Если есть вещь, именуемая в рисунке «революционный стиль» — это стиль 
        наших окон. <br>
        <br>
        Не случайно, что многие из этих работ, рассчитанные на день, пройдя Третьяковскую 
        галерею, выставки Берлина и Парижа, стали через десять лет вещами настоящего 
        так называемого искусства. <br>
        <br>
        Я привожу в этой книге только незначительную часть материала, только то, 
        что сохранилось в днях. Кроме двух, приводимых раньше по памяти, а теперь 
        полностью — текстов «Азбуки» и «Бубликов»,— все остальное не публиковалось 
        и публиковаться, кроме этой книги, не будет. <br>
        <br>
        Для меня эта книга большого словесного значения, работа, очищавшая наш 
        язык от поэтической шелухи на темах, не допускающих многословия. <br>
        <br>
        Это не столько чтение, сколько пособие для времен, когда опять придется 
        крикнуть: <br>
        <br>
        Голой рукой нас не возьмешь! <br>
        Деникина день сосчитан. <br>
        Красная Армия — красный еж — <br>
        верная наша защита. <br>
        Голой рукой нас не возьмешь! <br>
        Час Колчака сосчитан. <br>
        Красная Армия — красный еж — <br>
        лучшая наша защита. <br>
        Голой рукой нас не возьмешь! <br>
        Товарищи, все за оружие! <br>
        Красная Армия — красный еж – <br>
        железная сила содружия.<br>
        <br>
        В. М. <br>
        <br>
        (1929)<br>
        <br>
        <br>
                
        <div align="center"><strong>ОКНА САТИРЫ РОСТА<br>
          (Статья к открытию выставки в Третьяковке)</strong><br>
         <br></div>
        
        Сейчас в Третьяковской галерее в трех комнатах открыта выставка одиноких, 
        разрозненных, тщательно собранных окон сатиры РОСТА. В 19 — 21 годах тысячи 
        этих окон и радовали и мозолили глаза с множества окон и витрин пустующих 
        магазинов, клубных стен, вокзальных агитпунктов. <br>
        <br>
        Большинство этих окон растеряла наша безалаберность. <br>
        <br>
        Через годы над этими окнами будут корпеть ученые, охраняя от времени скверненькую 
        бумагу. <br>
        <br>
        Охранять эти окна надо и надо. <br>
        <br>
        Так как - <br>
        <br>
        это — красочная история трех боевейших годов Союза – <br>
        <br>
        так как - <br>
        <br>
        это — предки всех советских сатирических журналов, предки труднейшего, 
        безбумажного, безмашинного, ручного времени. <br>
        <br>
        Первые окна — уникумы, рисовались в одном экземпляре, дальнейшие размножались 
        в десятках и сотнях экземпляров трафаретом. Основные плакатщики: Черемных, 
        Малютин, я. <br>
        <br>
        Лозунги и тексты — почти все мои. Привожу несколько моих неопубликованных 
        окон и текстов. <br>
        <br>
          В ближайшее время в Гизе выйдет книга Окон Сатиры. <br>
         <br>январь 
