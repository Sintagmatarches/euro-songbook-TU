<strong>Paris, 1934</strong><br>
<br>
A man in the worn out clean overcoat<br>
Is walking along Montparnasse<br>
He looks like a Great War invalid<br>
Who are forgotten by all.<br>
<br>
He doesn't speak French very well.<br>
He' just an old soldier<br>
His every wound aches<br>
When rains knock on the roofs<br>
<br>
He'll suffer his pain with a smile and no tears<br>
Though may be was hungry for days<br>
Who knows if his old tuberculosis<br>
Would be healed by the City of Lights<br>

<br>
Poets, actors, artists are here<br>
Sitting in the open cafes<br>
And the white émigrés are here as well<br>
Still wearing the old Cossack uniform<br>
<br>
He likes arts but he 's not a poet<br>
He made a different choice<br>
Life usually doesn't present too many options,<br>
It's a no win situation in the end.<br>
<br>
He gave all away to the others, left nothing for himself<br>
While death was taking away his friends<br>
He knows how death is afraid of him<br>
He is intimately acquainted with it.<br>
<br>
There is no need in the map of Paris<br>
He knows everything about Paris<br>
Eiffel_Tower is saluting him<br>
Over the rows of the Montmartre's roofs.<br>
<br>
He knows all of Bois de Boulogne<br>
Where he likes to walk with his daughter<br>
He knows abou Père Lachaise cemetery<br>
Where he's going to lie soon.<br>
<br>
The writing on the stone reminds for a moment<br>
Of what had happened long ago<br>
Here is buried the soviet combrig<br>
Nestor Makhno.<br>
