	<span class="header"><strong>ПАРИЖ, 
1934 </strong><br></span>
<br>
Стихи и музыка: Лариса и Дмитрий Бергер<br>
<br>
По Монпарнасу идёт человек<br>
В поношенном чистом пальто.<br>
Он похож на великой войны калек,<br>
Которых не помнит никто.<br>
<br>
Он по-французки с трудом говорит,<br>
Он просто старый солдат.<br>
И каждая рана его болит,<br>
Когда дожди стучат.<br>
<br>
Он вытерпит боль с улыбкой, без слёз,<br>
Хоть, может, не ел пару дней.<br>
Кто знает, вдруг старый туберкулёз<br>
Сможет вылечить Город Огней?<br>
<br>
Поэты, артисты, художники здесь<br>
Сидят в открытых кафе.<br>
И белые эмигранты есть<br>
В старых донских галифе.<br>
<br>
Он любит искусство, но он не поэт,<br>
Он сделал выбор другой.<br>
Ведь в жизни часто и выбора нет,<br>
А если и есть, то плохой.<br>
<br>
Всё отдал другим, себе - ничего,<br>
А смерть забирала друзей.<br>
Он знает, как смерть боится его,<br>
Он близко знаком с ней.<br>
<br>
Карта Парижа ему ни к чему.<br>
Он знает всё про Париж.<br>
Эйффеля башня кивает ему<br>
Сквозь строй монмартских крыш.<br>
<br>
Он знает всё про Булонский лес,<br>
Где с дочкой любит гулять.<br>
Знает про кладбище Пер-Лашез,<br>
Где скоро будет лежать.<br>
<br>
Надпись на камне напомнит на миг<br>
О том, что было давно.<br>
Здесь похоронен советский комбриг<br>
Нестор Махно.<br>
<br>
Не позднее 2008<br>
