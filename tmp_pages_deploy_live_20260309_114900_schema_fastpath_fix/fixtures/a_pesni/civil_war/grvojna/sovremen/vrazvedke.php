<br>
Поворачивали дула<br>
В синем холоде штыков,<br>
И звезда на нас взглянула,<br>
Из-за дымных облаков.<br>
<br>
Наши кони шли понуро,<br>
Слабо чуя повода.<br>
Я сказал ему: - Меркурий<br>
Называется звезда.<br>
<br>
Перед боем больно тускло,<br>
Свет свой синий звезды льют...<br>
И спросил он: <br>
- А по-русски<br>
Как Меркурия зовут?<br>
<br>
Он сурово ждал ответа;<br>
И ушла за облака<br>
Иностранная планета,<br>
Испугавшись мужика.<br>
<br>
Тихо, тихо...<br>
Редко, редко<br>
Донесется скрип телег.<br>
Мы с утра ушли в разведку,<br>
Степь и травы - наш ночлег.<br>
<br>
Тихо, тихо... <br>
Мелко, мелко<br>
Полночь брызнула свинцом, -<br>
Мы попали в перестрелку,<br>
Мы отсюда не уйдем.<br>
<br>
Я сказал ему чуть слышно:<br>
- Нам не выдержать огня.<br>
Поворачивай-ка дышло,<br>
Поворачивай коня.<br>
<br>
Как мы шли в ночную сырость,<br>
Как бежали мы сквозь тьму -<br>
Мы не скажем командиру,<br>
Не расскажем никому.<br>
<br>
Он взглянул из-под папахи,<br>
Он ответил:<br>
- Наплевать!<br>
Мы не зайцы, чтобы в страхе<br>
От охотника бежать.<br>
<br>
Как я встану перед миром,<br>
Как он взглянет на меня.<br>
Как скажу я командиру,<br>
