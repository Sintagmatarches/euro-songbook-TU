        1927, слова<br>
        Не позднее 1982, музыка<br>
<br>
<br>
        Песня из репертуара трио &quot;Меридиан&quot;. Сегодня исполняется Аленой 
        Свиридовой. Микаэл Таривердиев начал работать с &quot;Меридианом&quot; 
        в 1980 году. В 1982 году вышла пластинка &quot;Ансамбль &quot;Меридиан&quot; 
        поет песни М. Таривердиева&quot;, где в числе 15 песен была и &quot;В 
        разведке&quot;. <br>
		<br>
        Это &quot;универсальная&quot; песня о Гражданской войне: из текста не 
        ясно, на чьей стороне воюют герои - красные они, белые или еще какие-нибудь. 
        Светлов писал, конечно, о красных, хотя Екатеринослав, где прошла его 
        красноармейская комсомольская юность, в ходе войны по нескольку раз был 
        и красным, и петлюровским, и махновским. В одном из своих стихотворений 
        (&quot;Колька&quot;) Светлов рассказывает, как должен был расстрелять 
        пленного махновца, а вместо этого с ним напился.<br>
<br>
Есть слегка фольклоризованный вариант этой песни (см. <a href="../bel-dvor/povoratch.php">&quot;Поворачивайте 
дула&quot;</a>). Принципиальное изменение там только одно: фраза <em>&quot;Мы 
не зайцы, чтобы в страхе От охотника бежать&quot;</em> заменена на <em>&quot;Мы 
не зайцы, чтобы в страхе От разбойников бежать&quot;</em>, - но от этого изменения 
песня стала смахивать на белогвардейскую.<br>
