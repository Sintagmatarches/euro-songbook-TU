  <br>
  Му, Pierwsza Brygada... <br>
  <br>
  <br>
  <img src="my1.gif" width="756" height="900"><br>
  <br>
  <br>
  Jest to najsłynniejsza pieśń Legionów Polskich utworzonych przez Józefa Piłsudskiego 
  i najsłynniejsza pieśń zołnierska z okresu I wojny światowej. Po wojnie stała 
  się nieoficjalnym hymnem Wojska Polskiego, а także była jedną z kandydatek do 
  miana hymnu państwowego. <br>
  <br>
  Pieśń powstawała w lipcu i jesienią 1917 roku. Za autorów jej tekstu uznani 
  zostali dwaj legioniści: Tadeusz Biernacki i Andrzej Hałaciński. Melodie zapożyczono 
  z rosyjskiego marsza wojskowego <em>Przjeście przez Morze Czerwone</em> (nieznanego 
  autora).<br>
  <br>
  Historia tej pieśni rozpoczęła się w sierpniu 1914 roku, kiedy to zorganizowane 
  przez Józefa Piłsudskiego bataliony związków strzeleckich złożone z trzech tysięcy 
  ochotników, na czele z I Kompanią Kadrową - ро marszu z Krakowa wkraczały do 
  Kielc; 12 sierpnia weszła do miasta "kadrówka&quot;.<br>
  <br>
  Przed wejściem Polaków, z Kielc wycofał się syberyjski pułk piechoty wojsk carskich, 
  który tu, na zachodnich krańcach imperium rosyjskiego, stacjonował w roku wybuchu 
  wojny. Nuty syberyjskiej orkiestry wojskowej przejął kapelmistrz Sikorski, prowadzący 
  w Kielcach orkiestrę dętą straży pożarnej. Wśród nich był anonimowy marsz <em>Przejście 
  przez Morze Czerwone</em>, które to nuty Sikorski oznaczył jako <em>Marsz nr 
  10</em>, na pamiątkę uwięzienia przez władze carskie jednego z członków orkiestry, 
  Czarneckiego, w Х Pawilonie. Kieleccy strażacy nauczyli się melodii tego marsza 
  i chętnie ją grywali podczas codziennych koncertów letnich w miejskim parku.<br>
  <br>
  Po wkroczeniu do miasta żołnierzy polskich, cała orkiestra Sikorskiego przyłączyła 
  się do legionistów, stając się później wojskową orkiestrą I Brygady Legionów 
  Polskich. <br>
  <br>
  Natomiast ów <em>Kielecki marsz dziesiąty</em> grała żołierzom już od pierwszych 
  wrześniowych bitew pod Nowym Korczynem i Laskami, а w listopadzie pod Krzywopłotami.<br>
  <br>
  W latach 1914-1917 melodia ta spopularizowała się wśród legionistów i kilkakrotnie 
  probówano do niej pisać teksty pieśni, które jednak dłużej nie przetrwały. А 
  były to anonimowe pieśni zaczynające się słowami: <em>Legiony to żebracza nuta</em> 
  (śpiewana już w 1915), <em>Legiony to zwycięska nuta</em>. Śpiewano też na melodię 
  rosyjskiej pieśni rewolucyjnej <em>Legiony to są Termopile. </em><br>
  <br>
  U źródeł tekstów tych anonimowych, jak również później tekstów Biernackiego 
  i Hałacińskiego, które utworzyły ostateczny kształt tej pieśni, leżała sytuacja, 
  z jaką w społeczeństwie polskim spotkali się legioniści. Kiedy strzelcy Piłsudskiego 
  w sierpniu 1914 roku przekraczali granicę między zaborem austriackim i rosyjskim, 
  szli z ogromnym ładunkiem nadziei, w przekonaniu, że idą oswobodzić kraj. Jednakże 
  już na początku spotkali się z chłodem i wyraźną rezerwą ze strony rodaków. 
  Ludność cywilna tzw. kongresówki, czyli ziem polskich wchodzących w skład imperium 
  rosyjskiego, nie dowierzała, że rozpętana niedawno wojna światowa oraz sformowane 
  w Austrii wojsko polskie - przywraca Polsce niepodległość. Najpierw Kielce, 
  а potem Warszawa, raczej obojętnie patrzyły w 1914 roku na wojsko Piłsudskiego, 
  со w żołnierzach budziło gorycz i zwątpienie.<br>
  <br>
  Dramatyczny moment w historii Legionów Polskich nastąpił w lipcu 1917 roku, 
  podczas tzw. kryzysu przysięgowego. Niemcy zażądali bowiem od oddziałów polskich 
  złożenia przysięgi na dotrzymanie wierności i braterstwa broni wojskom niemieckim 
  i austro-węierskim. Legioniści odmówili. Józef Piłsudski z pułkownikiem Sosnkowskim 
  zostali uwięzieni w twierdzy magdeburskiej. Natomiast rodacy negatywnie ocenili 
  postawę żołnierzy Piłsudskiego, uznając ją za niepolityczną i niebezpieczną 
  w skutkach.<br>
  <br>
  Badacz poezji legionowej, Grzegorz Bytnar (właśc. Andrzej Romanowski), pisał: 
  "Legioniści poczuli się nagle odepchnięci przez całe społeczeństwo - przez wszystkie 
  jego partie, frakcje, obozy. Chłopcom w maciejówkach przychodzila zapewne ochota 
  wygarnięcia rodakom prawdy. Takim właśnie gorzkim, brutalnym rozrachunkiem była 
  <em>Му, Piervsza Brygada.</em> Polacy - zdawała się mówić pieśń - nie dorośli 
  do czynu legionowego...&quot;<br>
  <br>
  Wydarzenia z okresu kryzysu przysięgowego stały się bezpośrednią przyczyną napisania 
  tekstu tej pieśni, który to tekst pisali niezależnie od siebie, w dwóch różnych 
  miejscach Europy - Tadeusz Biernacki i Andrzej Hałaciński. <br>
  <br>
  Biernacki, 18-letni wówczas żołnierz, twierdził, że swój tekst pieśni napisał 
  w nocy z 17 па 18 lipca w pociągu z Modlina do Szczypiórna, wiozącym internowanych 
  przez Niemców legionistów, którzy odmówili przysięgi. Natomiast wcielony, ро 
  kryzysie przysięgowym, do armii austriackiej legionista I Brygady i literat 
  Andrzej Tadeusz Hałaciński (więzień Kozielska, zamordowany w 1940 w Katyniu) 
  - podał, iż swój tekst napisał w południowym Tyrolu, na froncie włoskim, jesisnią 
  1917 roku. Аlе саłу czas pozoptawał pod silnym wpływem emocji lipcowego kryzysu. 
  Na przykład ро głośnym "Liście otwartym do brygadiera Piłsudskiego, byłego członka 
  Rady Stanu&quot; Izy Moszczeńskiej-Rzepeckiej opublikowanym w "Głosie&quot; 
  (22 lipca 1917), który Hałaciński uznał za "niepoczytalny w swej bezmyślnej 
  złośliwości artykuł zarzucający warcholstwo Komendantowi i nam&quot; - odpowiedział 
  jesienią w jednej ze swoich zwrotek pieni (nie weszła ona potem do wspólnego 
  tekstu, lecz znalazła się tam bardzo podobna zwrotka Biernackiego). Odpowiedź 
  Hałacińskiego była dosadna i sformułowana żołnierskim językiem: "Nie chcemy 
  już od was uznania, ni waszych mów, ni waszych łez. Skończyły się dni kołatania 
  do waszych dusz - jebał was pies!&quot;<br>
  <br>
  Po raz pierwszy cała pieśń, tekst i melodię, opublikowano w Warszawie w 1920 
  roku nakładem В. Rudzkiego - pod tytułem <em>Му, pierwsza brygada. Piosenka 
  żołnierska w ukiadzie М. Kozar-Słobodzkiego </em>(autor tego opracowania to 
  także twórca melodii pieśni <em><a href="rozkwitaja.htm">Rozkwitają pąki białych 
  róż</a></em>). <br>
  <br>
  10 sierpnia 1924 roku, na Zjeździe Legionowym w Lublinie, marszałek Józef Piłsudski 
  powiedział: "Dziękuję panom za najdumniejszą pieśń, jaką kiedykolwiek Polska 
  stworzyła&quot;. Po tym zjeździe, w sierpniu 1924 Biernacki dopisał zwrotkę 
  "Dzisiaj już my jednością silni&quot;, а Hałaciński w październiku 1925 ostatnią 
  zwrotkę "Potrafim dziś dla potomności."<br>
  <br>
  Od początku lat 20. popularność tej pieśni nigdy nie malała wśród Polaków, bez 
  względu na czas historyczny pokoju lub wojny i okoliczności polityczne oraz 
  obowiązujący ustrój. <br>
  <br>
  Pieśń jest znana też pod tytułem <em>Marsz Pierwszej Brygady, Pierwsza Brygada.</em><br>
  <br>
  ***<br>
  <br>
  Tadeusz BIERNACKI (1899-1974), inżynier chemik i metaloznawca, autor wierszy, 
  podpułkownik Wojska Polskiego. Вуł żołnierzem I Brygady w kompanii L. Lisa-Kuli. 
  W 1917 napisał część zwrotek tekstu pieśni <em>Му, Pierwsza Brygada</em> (drugim 
  współautorem tekstu tej pieśni jest А. Т. Hałaciński). W czasie II wojny światowej 
  pracował w polskim Wojskowym Instytucie Technicznym w Wielkiej Brytanii, ро 
  wojnie pozostał w Londynie. W 1946 Rada Ministrów PRL pozbawiła go obywatelstwa 
  polskiego. Zmarł w Szwajcarii. <br>
  <br>
  Andrzej Tadeusz HAŁACIŃSKI (1891-1940), autor utworów literackich, podpułkownik 
  Wojska Polskiego, notariusz, starosta w Brzesku i Okocimiu. Do kryzysu przysięgowego 
  1917 był żołnierzem I Brygady Legionów, w czasie kryzysu napisał część tekstu 
  pieśni <em>Му, Pierwsza Brygada</em> (drugi współautor tekstu: Т. Biernacki). 
  Internowany w Kozielsku, został zamordowany przez NKWD w Katyniu. <br>
