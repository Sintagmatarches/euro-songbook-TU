  1. Как на матушке на Неве-реке</span><br>
  <br>
  Как на матушке на Неве-реке, <br>
  На Васильевском славном острове, <br>
  Молодой матрос корабли снастил <br>
  О двенадцати тонких парусах, <br>
  Тонких, белых да полотняных. <br>
  Из высокого нова терема <br>
  Как увидела красна девица — <br>
  Взяла ведерка, за водой пошла, <br>
  Зачерпнула, ведра поставила, <br>
  Поставивши, призадумалась, <br>
  Призадумавшись, слово молвила: <br>
  «Уж ты гой ли есть, молодой матрос! <br>
  Что не вовремя корабли снастишь?» — <br>
  «Ах ты, глупая красна девица, <br>
  Неразумная дочь отецкая! <br>
  Не своей волей корабли снащу – <br>
  По указу я государеву, <br>
  По приказу я адъютантскому». <br>
  Красна девица подняла ведра, <br>
  Подняла ведра, ко двору пошла. <br>
  И увидела чудо чудное, <br>
  Чудо чудное, диво дивное: <br>
  Не огонь горит, не смола кипит, <br>
  Тут кипит сердце молодецкое. <br>
  Не по батюшке, не по матушке, <br>
  По душе красной девице. <br>
  Что от девушки пришла весточка: <br>
  «Красна девица немочна лежит». <br>
  По-за весточкой снова грамотка: <br>
  «Красна девица уж преставилась». <br>
  «Я поеду к церкви Божественной, <br>
  Привяжу коня к колоколенке, <br>
  Уж я хлопнуся о сыру землю, <br>
  Ты прими меня, мать сыра земля, <br>
  Во могилушку к красной девице». <br>
