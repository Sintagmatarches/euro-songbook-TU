<span class='i'>2. Вниз по матушке, по Неве-реке...</span><br>
 <br>
 Вниз по матушке, по Неве-реке, <br>
  Что на пристани кораблиной <br>
  Молодой матрос корабли снастил <br>
  Об двенадцати парус тонкиих, <br>
  Парусы тонкие, полотняные. <br>
  Как увидела красная девушка <br>
  Из высокого красного терема, <br>
  Из косящатого окошечка: <br>
  «Ох, ты гой еси, молодой матрос! <br>
  Ты на что рано корабли снастил <br>
  Об двенадцати парус тонкиих?» — <br>
  «Ох ты, глупая, красна девица, <br>
  Не своей волей я корабли снастил, <br>
  По указу государеву, <br>
  По приказу я командирскому. <br>
  Под кустиком, под ракитовым <br>
  Не огонь горит, не смола кипит, <br>
  Что кипит сердце молодецкое <br>
  Не по батюшке, не по матушке, — <br>
  Что по душечке красной девице». <br>
