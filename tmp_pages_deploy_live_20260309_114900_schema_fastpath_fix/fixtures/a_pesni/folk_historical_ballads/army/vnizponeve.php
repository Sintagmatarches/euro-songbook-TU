	<h1>КАК НА МАТУШКЕ НА НЕВЕ-РЕКЕ</h1>
	<br>
	
  Как на матушке на Неве-реке, <br>
  На Васильевском славном острове <br>
  Молодой матрос корабли снастил, <br>
  Корабли снастил, мачты-парусы, <br>
  <br>
  Корабли снастил, мачты-парусы <br>
  О двенадцати тонких парусах, <br>
  Тонких, белыих, полотняныих, <br>
  Полотняныих, шитых-браныих. <br>
  <br>
  Увидала то красна девица, <br>
  Красна девица, дочь купеческа, <br>
  Взяла ведерцы, за водой пошла <br>
  На Неву-реку воду черпати. <br>
  <br>
  Почерпнувши, ведры поставила, <br>
  А поставивши, слово молвила: <br>
  «Ох ты, гой еси, молодой матрос, <br>
  Почему, матрос, рано так встаешь, <br>
  <br>
  Рано так встаешь, по Неве плывешь?» <br>
  «Я поведаю красной девице: <br>
  По указу-то государскому, <br>
  По приказу, приказу царскому!» <br>
