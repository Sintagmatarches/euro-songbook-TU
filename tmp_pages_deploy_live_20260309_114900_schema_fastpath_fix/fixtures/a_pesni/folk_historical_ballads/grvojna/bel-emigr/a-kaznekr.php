  <br>
  <sup>2</sup> Русский воинский некрополь Софийского городского кладбища, рукопись, 
  личный архив автора. <br>
  <br>
  <sup>3</sup> Русский воинский некрополь кладбища Княжево-Софийско, рукопись, 
  личный архив автора. <br>
  <br>
  <sup>4</sup> Русский воинский некрополь кладбища Ольшаны. //Русское прошлое. 
