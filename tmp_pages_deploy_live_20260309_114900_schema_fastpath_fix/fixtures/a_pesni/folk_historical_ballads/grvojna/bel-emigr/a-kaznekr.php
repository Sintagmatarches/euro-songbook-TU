        <span class="text"><strong>В.Г. Чичерюкин-Мейнгардт</strong><br>
  <br>
  <span class="header"><strong>КАЗАЧИЙ НЕКРОПОЛЬ РУССКОГО ЗАРУБЕЖЬЯ</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 305-306.<br>
  <br>
  </em><br>
  Из осмотренных автором некрополей Русского Зарубежья в Европе и США, запомнилось 
  отсутствие казачьих могил на православном кладбище Гельсингфорса (Финляндия), 
  что вполне объяснимо. При посещении кладбища Тегель в Берлине в августе 2001 
  г. автору удалось выявить лишь две казачьи могилы. Последнее обстоятельство 
  не может не вызвать удивления, потому что Берлин был одной из столиц Русского 
  Зарубежья до 1945 г. Кроме того, именно в Берлине в 1943-1945 гг. находилось 
  Главное управление казачьих войск Германской империи. Буквально в двух метрах 
  друг от друга расположены могилы бывшего командующего Донской армией ВСЮР генерал-лейтенанта 
  В.И. Сидорина (1882-1943) и его начальника штаба генерал-лейтенанта А.К. Келчевского 
  (1869-1923). На дощечке с именем генерала Сидорина указан его генеральский чин. 
  В то время как на дощечке с именем генерала Келчевского чин не указан. Вместе 
  с генералом Келчевским похоронена и его супруга (1924 г.). Рядом с их именами 
  надпись: «Россия — Родина помнит вас»<sup>1</sup>. <br>
  <br>
  Несколько казачьих могил сохранилось на Софийском городском кладбище, осмотренном 
  автором в 2002-2003 гг. Несколько казаков погребено на участке (парцеле) №91, 
  специально выделенном болгарскими властями для погребения русских ветеранов 
  Освободительной войны 1877-1878 гг. еще в 1920-х гг. На соседних участках, где 
  хоронили русских эмигрантов, в большинстве своем участвовавших в Белом движении, 
  также сохранились казачьи могилы. Как известно, в 1921 г. с острова Лемнос в 
  Болгарию были перевезены донские казачьи части под командованием генерал-лейтенанта 
  Ф.Ф. Абрамова. Автору запомнилась могила русского эмигранта П.А. Пелиха (1896-1961) 
  в форме кубанского или терского казака. На нем кубанка и черкеска с газырями. 
  Остальные казаки, погребенные на участках № 93 и 94, — донцы. Помимо надписи 
  «донской казак», в большинстве случаев встречалось имя погребенного и название 
  его родной станицы - Гундоровская, Мигулинская, Верхне-Мирская. <br>
  <br>
  На Софийском городском кладбище похоронены казаки-донцы: генерал-майор М.А. 
  Александров (1865-1951), полковник А. Ф. Грузинов (1883?-1935?) — донской артиллерист; 
  полковник А.Н. Плетнев (1876-1950), преподаватель Атаманского военного училища; 
  простые казаки Е.К. Брыков (1897-1952), П.Я. Колзунов (1881-1942), Х.П. Макаров 
  (Х.П. Марков) (1872-1934?), Я. Назаров (1882-1936).<sup>2</sup><br>
  <br>
  Несколько казачьих могил сохранилось до сегодняшнего дня на русском участке 
  кладбища Княжево-Софийско. В этом пригороде болгарской столицы до 1944 г. располагался 
  Русский инвалидный дом, принадлежавший Болгарскому отделению Зарубежного союза 
  русских военных инвалидов. Комплекс зданий сохранился по сей день.<sup>3</sup><br>
  <br>
  На Ольшанском кладбище в Праге также сформировался свой казачий некрополь. В 
  крипте храма Успения Пресвятой Богородицы, что на Ольшанах, в разное время были 
  похоронены генерал-лейтенант Т.А. Семерников (1949) — директор Донского казачьего 
  корпуса; кубанский казак Ф.А. Щербина (1849-1936) — общественный деятель, профессор; 
  донской казак Н.А. Келин (1894-1970) — доктор медицины, поэт, автор известной 
  книги «Казачья исповедь». На участках, прилегающих к Успенской церкви, сохранились 
  могилы донских и кубанских казаков. Братья Пухляковы Василий Николаевич и Николай 
  Николаевич, полковники Всевеликого войска Донского, в эмиграции проживали в 
  Праге. В.Н. Пухляков (1879-1943) умер в Праге во время Второй мировой войны. 
  Н.Н. Пухляков (1881-1949) в мае 1945 г. был арестован в Праге советскими чекистами 
  и вывезен в СССР. Он умер в лагерях, и на Ольшанах его символическая могила. 
  Еще одна символическая могила запомнилась на соседнем участке. Это могила белого 
  офицера, первопоходника Н.П. Цветкова (1891-1952). Цветков изображен на керамическом 
  фото в кубанке, черкеске с газырями. На груди орден Св. Георгия и знак за Первый 
  Кубанский поход. Указано место рождения — Царицын. Неизвестно, был ли Цветков 
  природным казаком, судя по форме, кубанским, или терским, или был приписан к 
  казачьей части во время Гражданской войны. Как и полковник Н.Н. Пухляков, Н.П. 
  Цветков погиб в советском лагере. <br>
  <br>
  Также вблизи Успенской церкви погребены донские казаки полковник В.В. Добрынин 
  (1884-1938), подъесаул И.И. Цапов (1888-1967), поэт А.И. Левицкий (1897-1980), 
  П.И. Прокопович (1884-1960), Г.Я. Филатов (1940). Здесь же похоронена и супруга 
  известного казачьего генерала Е.И. Балабина, Александра Балабина (ур. Воробьева) 
  (1935). Сохранилась могила кубанского казака хорунжего П.Ф. Думбровского (1887- 
  1939). Предположительно, был уральским казаком или служил в рядах Уральской 
  армии во время Гражданской войны полковник А.Н. Мизинов (1891-1945).<sup>4</sup><br>
  <br>
        На самом знаменитом погосте Русского Зарубежья автору довелось побывать 
        трижды — в 1994, 1995 и 2001 гг. Поскольку отечественным исследователем 
        И. Грезиным несколько лет тому назад был издан капитальный труд — «Алфавитный 
        список русских захоронений кладбища Сент-Женевьев де Буа», а еще раньше 
        петербургским историком В.Г. Бортневским был опубликован список казаков-гвардейцев, 
        похороненных на этом кладбище, автор ограничился тем, что сфотографировал 
        памятники на могилах атамана Всевеликого войска Донского в Зарубежье генерал-лейтенанта 
        графа М.Н. Граббе (1942), генерал-майора Б.В. Фроста (1876-1924) и подъесаула 
        И.Г. Михайлова (1897-1947). <br>
  <br>
  В июле 1999 г. автору дважды удалось посетить кладбище Холливуд в Лос-Анджелесе, 
  штат Калифорния, США. На нем есть несколько участков, которые условно можно 
  назвать русскими. Здесь же встречаются могилы казаков, причем, в основном, с 
  Юга a России, несмотря на то, что первыми в городах тихоокеанского побережья 
  США поселились участники Белого движения на Востоке России. Единственное исключение 
  — А.В. Корнилов (1874-1961). На его могильной плите начертано: «Генерал-майор 
  Сибирских казачьих войск». Остальные казаки — это донцы или кубанцы. Кубанские 
  казаки: генерал-майор М.А. Скворцов (1887-1967), войсковой старшина Н.Н. Черножуков 
  (1890-1964), есаул М.И. Маско (1893- 1960), хорунжий Д.Н. Галаган (1899-1979). 
  Донские казаки: сотник И.В. Машинцев (1890-1959), М.Ф. Ермилов (1890-1964), 
  П.Ф. Ермилов (1900-1981), М. Лаврухин (1892-1974), А. Любибогов (1888-1968), 
  В.К. Поляков (1896-?), П.Н. Прохоров (1899-1959). Есть на кладбище Холливуд 
  и единственная могила терского казака — П. Демнова (1898-1953).<sup>5</sup><br>
  <br>
  <br>
  <sup><em>1</em></sup><em> Русский воинский некрополь кладбища Тегель в Берлине, 
