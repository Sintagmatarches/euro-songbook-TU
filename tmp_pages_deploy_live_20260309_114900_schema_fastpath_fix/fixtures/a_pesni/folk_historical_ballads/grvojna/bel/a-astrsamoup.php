        <br>
        <span class="header"><strong>АСТРАХАНСКОЕ КАЗАЧЬЕ ВОЙСКО И РЕВОЛЮЦИЯ 1917 г.: 
        К ВОПРОСУ О РАЗВИТИИ КАЗАЧЬЕГО САМРУПРАВЛЕНИЯ НА ЮГЕ РОССИИ</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 153-155</em>.<br>
  <br>
  <br>
  Проблема развития казачьего самоуправления в 1917 г., попытка становления казачьих 
  войск как автономных субъектов новой революционной России интересна прежде всего 
  тем, что позволяет, отказавшись от набивших оскомину спорое в рамках дихотомии 
  «народ — сословие», посмотреть на казачество как на самобытное явление, его 
  место и роль в революции и Гражданской войне через проблему самоидентификации, 
  самоопределения казаков в условиях переломной революционной эпохи. <br>
  <br>
  Астраханское казачье войско — одно из старейших «указных», служилых войск России 
  — создавалось в XVIII в. ради замирения и колонизации степного пограничья, и 
  в этой службе главная историческая заслуга астраханских казаков. Накануне революции 
  1917 г. астраханское казачество являлось малочисленной замкнутой привилегированной 
  военно-земледельческой этно-сословной группой населения Нижнего Поволжья, существование 
  которой в данном регионе, по мнению государственных чиновников, не оправдывалось 
  уже ни военно-стратегическими, ни социально-экономическими потребностями государства. 
  <br>
  <br>
  К 1917 г. в войске насчитывалось около 40 тысяч казаков, проживавших в расположенных 
  вдоль русла Волги от Астрахани до Саратова 20 станицах с хуторами и рыбачьими 
  поселками. Войсковые земли, небольшими участками разбросанные на территории 
  Астраханской, Саратовской и Самарской губернии, нигде не образовывали сколько-нибудь 
  значительных собственно казачьих районов. При этом астраханские станицы находились, 
  частью рядом с крупными волжскими торгово-промышленными городами (или в самих 
  городах), частью были вкраплены среди крупных крестьянских поселений, тянувшихся 
  вдоль Волги до Каспийского моря. <br>
  <br>
  Войско, относившееся к младшим, «вторичным» войскам, создавалось сверху, государством, 
  в основном из неказачьего служилого населения Поволжья, по образу и подобию 
  Донского войска, и своих специфических, глубоких казачьих традиций (уходящих 
  корнями в казачью вольницу XVI—XVII вв.) у астраханцев, по большому счету, не 
  было. <br>
  <br>
  Система административно-территориального устройства у казаков, основным смыслом 
  существования которых являлась военная и полицейская служба, в период реформ 
  1860-1870 гг. и в пореформенный период не претерпела принципиального изменения. 
  11 казачьих войск империи, являясь существенной частью и важной опорой ее военно-бюрократической 
  системы, представляли собой четко организованные, подчинявшиеся военному министерству 
  жестко централизованные военно-административные и хозяйственные структуры, находившиеся 
  под полным государственным контролем и опекой. <br>
  <br>
  Во главе Астраханского войска стоял Наказный атаман (назначаемый сверху, из 
  неказаков), сосредотачивавший в своих руках всю полноту военной, гражданской 
  и судебной власти над станицами. По традиции должность атамана совмещалась с 
  должностью астраханского губернатора. Астраханское войско как самостоятельная 
  административная единица было приравнено к губернии, поэтому по войску атаман 
  обладал правами губернатора, а в военном отношении, исходя из числа казаков, 
  которых в случае войны астраханцы могли поставить в строй — правами начальника 
  дивизии. В военном отношении казачьи части входили в состав армейских корпусов 
  и дивизий, приписанных к определенным военным округам. Так Астраханское входило 
  — в Казанский военный округ, а первоочередной 1-й Астраханский казачий полк 
  — в состав 5-й кавалерийской дивизии (XVI армейского корпуса), расквартированной 
  в Поволжье. <br>
  <br>
  Управление войском по гражданской части сосредотачивалось в Войсковом правлении, 
  а по военной — в Атаманской канцелярии, чиновники которых назначались атаманом 
  и утверждались вышестоящим военным начальством. Среднее звено управления было 
  представлено администрацией двух отделов. Атаманы и Правления отделов также 
  назначались сверху. <br>
  <br>
  Низшее звено — станицы — по традиции, идущей от времен существования вольных 
  казачьих войск, обладали правом самоуправления «по гражданской части». Станичный 
  сход выбирал станичного атамана, правление и суд, которые станица содержала 
  за счет своих общественных капиталов. Однако в процессе огосударствления казачества 
  это самоуправление последовательно сводилось на нет, и к ХХ в. было во многом 
  формальностью, данью традиции. <br>
  <br>
  Хозяйство и быт казаков, были привязаны к длительной военной службе, к необходимости 
  быть в постоянной боеготовности, в ожидании мобилизации; при этом, коня, обмундирование, 
  амуницию и вооружение казак обеспечивал за собственный счет. Наряду с воинской 
  службой казаки были обложены рядом дополнительных обременительных «земских» 
  повинностей (содержание дорог, перевозов, караульная служба и т.д.). <br>
  <br>
  В начале ХХ в. казачьи войска, система казачьего хозяйства и службы, возникшие 
  и сформировавшиеся в период стремительной территориальной экспансии и колонизации 
  многонациональных окраин, переживали системный кризис. Сами казаки расценивали 
  свое состояние как полукрепостное и требовали облегчения повинностей, сокращения 
  сроков службы и числа казачьих частей, снятия жестких ограничений и регламентаций 
  военного сословия, регулирующих всю жизнедеятельность казака. Казачье хозяйство, 
  казачьи общины под влиянием развития капиталистических отношений деградировали, 
  теряли свою экономическую устойчивость, способность обеспечивать свое главное 
  предназначение — казачью службу. Набирал силу процесс внутреннего «саморасказачивания», 
  сближения казачьего населения с мещанским и крестьянским (по образу жизни, занятиям, 
  культуре). Процесс социально- имущественной дифференциации привел к появлению 
  слоев своего казачьего кулачества и пролетариата - батраков, отходников. <br>
  <br>
  В связи этим, идея ликвидации казачьих войск и системы казачьей службы, ликвидации 
  казачества как сословия не раз обсуждалась российской военно-бюрократической 
  элитой во второй половине XIX — начале ХХ вв. Однако как раз на рубеже XIX-ХХ 
  вв. системный кризис в России, резкое обострение социально-политической ситуации, 
  революционные потрясения актуализировали потребность в казаках, прежде всего, 
  как в полицейской силе. И казаки действительно хорошо послужили престолу, подавляя 
  революцию 1905-1907 гг. <br>
  <br>
  Государство потратило немало сил и средств для поднятия казачьих хозяйств, укрепления 
  войск, культивирования казачьей особости — той уникальной служилой культуры, 
  менталитета, которые делали казаков верной опорой монархии. Однако меры эти 
  носили паллиативный, поверхностный характер. Казачьи хозяйства не выдержали 
  испытания Первой мировой войной. Казачьи общины, и до того пораженные недугом 
  малоземелья, резкой имущественной дифференциацией, лишенные значительной части 
  рабочих рук, страдали от разорения, обнищания большей части даже крепких середняков. 
  <br>
  <br>
  Вероятно, при дальнейшем развитии России по буржуазно-демократическому пути, 
  сама логика этого развития неизбежно привела бы к упразднению казачьего сословия, 
  растворению казачьих этно-сословных групп. Однако Февральская революция 1917 
  г., падение самодержавия и особенности развития российского революционного процесса 
  создали предпосылки для уникального этапа в развитии Астраханского войска — 
  попытки становления войска как автономного субъекта новой революционной России. 
  <br>
  <br>
  В период Февральской революции казачья старшина, занимавшая в Астраханской губернии 
  привилегированное положение, выступила наряду с городской буржуазией и интеллигенцией 
  одним из главных инициаторов бескровного политического переворота. Опираясь 
  на казачьи части астраханского гарнизона, представители казачьего офицерства 
  и интеллигенции, арестовав астраханского губернатора и Наказного атамана, прежнюю 
  войсковую и губернскую администрацию, приступили к организации губернской и 
  войсковой жизни на новых, революционных началах. Казачьи представители заняли 
  лидирующее положение в губернской общественно-политической жизни, возглавили 
  новые губернские органы власти. <br>
  <br>
  На внутривойсковом уровне, казаками уже в первые революционные дни был сделан 
  шаг к «восстановлению» изначально не существовавших у астраханцев демократических 
  институтов вольного казачества и организации своей самостоятельной казачьей 
  власти (формирования самоуправления на уровне войска). Уже 4 марта в Астрахань 
  были созваны представители от станиц и полков войска на экстренный казачий Круг. 
  Делегаты постановили не позднее начала апреля созвать Большой Войсковой Круг, 
  который, как казачий парламент, должен был стать высшей законодательной и исполнительной 
  властью в войске. Главой исполнительно-распорядительной власти должен был стать 
  избираемый Кругом Войсковой атаман. <br>
  <br>
  Первый большой Астраханский Круг прошел 7-14 апреля 1917 г. Председателем Круга 
  и Войсковым атаманом казаки избрали видного представителя старшины Т.А. Соколова. 
  Круг утвердил предложенный атаманом новый состав Войскового правления и Атаманской 
  канцелярии, атаманов отделов и состав правлений отделов, основы местного самоуправления, 
  сроки созывов казачьих Кругов и службы выборных лиц на войсковых должностях. 
  Позднее, на 2-й сессии Войскового Круга (15-22 июня) делегаты утвердили состав 
  высшего органа законодательной и исполнительной власти на период между созывами 
  Кругов - Совет Войскового Круга. <br>
  <br>
  В течение марта у астраханцев прошли выборы новых станичных атаманов и реорганизация 
  станичной администрации на основах широкого самоуправления (станичные правления 
  упразднены, организованы исполкомы); казаки отказались от несения полицейской 
  службы, даже в собственных станицах. <br>
  <br>
  С первых шагов становления новой казачьей власти, среди казаков обозначилась 
  тенденция на обособление от неказачьего населения, недопущение вмешательства 
  в жизнь войска и станиц неказачьих органов власти (как советских, так и Временного 
  правительства). Казаки однозначно выступали за сохранение войска как особого 
  автономного образования в составе России. <br>
  <br>
  Определяя свою политическую позицию, казачьи съезды принимали резолюции об оказании 
  полной поддержки Временному правительству и войне до победного конца, приверженности 
  идее Всероссийского Учредительного собрания. Самым болезненным вопросом для 
  всех кругов весной-летом 1917 г. был земельный вопрос и связанный с ним вопрос 
  отношений с крестьянским, иногородним населением. Казаки выступили резко против 
  идеи уравнения с крестьянским населением и уравнительного передела земель. Наоборот, 
  станичники требовали увеличивать, прикупать земельные наделы, отбирать в пользу 
  станиц частные, монастырские земли, участки, арендованные иногородними. <br>
  <br>
  Таким образом, астраханское казачество, в целом, положительно приняло Февральскую 
  революцию как изменение, отвечавшее его собственным потребностям (ожидая решения 
  своих наболевших проблем — облегчение службы, увеличение земель, ликвидация 
  сословных ограничений, при расширении прав и привилегий); решительно выступило 
  за демократизацию страны, расширение самоуправления, отказ от полицейских функций; 
  проявило себя последовательным сторонником буржуазно-демократического лагеря. 
  Казаки заявили себя как сторонники демократической федеративной республики, 
  при широкой автономии казачьих войск в составе России. <br>
  <br>
  Вместе с тем, астраханцы, ощущая себя частью привилегированной замкнутой этно-сословной 
  казачьей корпорации, с первых же шагов приняли активное участие в организации 
  общеказачьей общественно-политической жизни, участвовали во всех общеказачьих 
  съездах, создании Совет Союза казачьих войск; поддерживали идеи создания особой 
  казачьей государственности, казачьей армии, пытаясь выступать и как представители 
  особого народа, со своими особыми от других народов России интересами. Но наиболее 
  важным направлением деятельности для астраханцев было налаживание контактов 
  и формирование Союза казачьих войск юга России, основной задачей которого казакам 
  виделась борьба с анархией и большевизмом, доведение России до Учредительного 
  собрания во имя создания демократической республики. <br>
  <br>
  Летом 1917 г. казачья старшина дистанцировалась от неказачьих проблем, сосредоточившись, 
  прежде всего, на внутривойсковой и общероссийской казачьей жизни. В казачьей 
  среде, в целом, возобладало стремление к сословной замкнутости, консолидации 
  с представителями других казачьих войск, освобождению от тяготящих обязанностей 
  и функций служилого сословия, при сохранении преимуществ и привилегий, увеличении 
  земельных владений. Вместе с тем, обозначилось и нарастание противоречий между 
  казачьим и крестьянским населением региона, внутрисословные противоречия — между 
  консервативной старшиной и казаками старших возрастов; радикально настроенными 
  фронтовиками и молодежью, между зажиточными и беднейшими слоями. <br>
  <br>
  Становление казачьего самоуправления в период двоевластия сопровождалось периодическими 
  конфликтами городских (а затем и сельских) станиц с местными Советами, требовавшими 
  от казаков признания власти неказачьих исполкомов, присылки в их состав делегатов 
  от казаков, подчинения их решениям по земельному, продовольственному и другим 
  вопросам. <br>
  <br>
  Однако наиболее важной и опасной для войсковой верхушки проблемой была поддержка 
  казачьими частями Советов солдатских депутатов. При этом, здесь, как и в других 
  важных политических вопросах, казаки занимали противоречивую позицию. С одной 
  стороны, казачьи полки сохраняли дисциплину, лояльность правительству и командованию 
  и соглашались даже выполнять роль своеобразных заградотрядов в период июньско-июльского 
  наступления, занимались восстановлением порядка в пехотных частях. С другой 
  стороны, попытки казачьей старшины, войсковых органов власти отозвать из Советов 
  казачьих делегатов встречали жесткое противодействие строевиков. Только в августе 
  по примеру Донского войска, астраханской старшине удалось решить эту проблему 
  (прежде всего при опоре на казачью спайку, корпоративность, апеллируя к «голосу 
  всего казачества»). Т.е. идея сохранения сословного единства была чрезвычайно 
  сильна среди станичников. <br>
  <br>
  Осенью 1917 г., в условиях нарастающей анархии, разрухи, казаки с одной стороны, 
  в пику своим прежним настроениям приняли решение поддерживать порядок в Астрахани 
  силами казачьих частей, с другой — на практике, все больше игнорировали не только 
  советы, но и местные административные органы Временного правительства. Наиболее 
  популярной среди астраханцев становиться идея своей особой «казачьей» политической 
  линии. <br>
  <br>
  В конце сентября - начале октября 1917 г. на проходившем в Астрахани очередном 
  Большом Войсковом Круге среди делегатов от полков и станиц войска преобладали 
  консервативные, «казакийские» настроения. Казаки заявили о поддержке донского 
  атамана А.М. Каледина и «правого» политического курса Донского войска. Председателем 
  Круга был избран лидер астраханских кадетов, присяжный поверенный Н.В. Ляхов, 
  Войсковым атаманом — лидер консерваторов генерал-майор И.А. Бирюков. В условиях 
  развала государственности и нарастания центробежных тенденций, избранный делегатами 
  Совет Войскового Круга (а реально - выделенный из его состава Малый совет) был 
  наделен всей полнотой власти по войску. При этом, выступая за создание казачьей 
  республики, делегаты защищали тезис отказа от поддержки какой-либо российской 
  партии (включая кадетов) — «казаки вне политики». <br>
  <br>
  В ответ на захват государственной власти большевиками в октябре 1917 г., Войсковое 
  правительство, созданное атаманом Бирюковым на базе Войскового правления, не 
  признав большевицкий Совнарком, заявило о поддержке Временного правительства 
  и взяло курс на фактическую независимость Астраханского войска. 20 октября астраханцы 
  вошли в сформированный донским атаманом А.М. Калединым антибольшевицкий «Юго-Восточный 
  Союз» — военно-политический союз казачьих войск. Кроме того, по соглашению с 
  донской верхушкой, калмыцкой знатью и интеллигенцией (как часть программы создания 
  антибольшевицкого военно-политического союза на юге), в ноябре-декабре 1917 
  г. в Астраханское войско было принято около 160 тысяч астраханских калмыков, 
  на правах федератов, с сохранением внутренней независимости калмыков по гражданской 
  части, при «военно-административном» единстве (один Войсковой атаман, один Войсковой 
  штаб, созданный из атаманской канцелярии, два помощника атамана (по коренной 
  и калмыцкой части), два Войсковых Круга и правительства. Основной целью этого 
  союза для калмыков являлось объединение в одно целое астраханских, ставропольских 
  (а в перспективе и донских) калмыков, их оказачивание (как наиболее приемлемая 
  форма национального объединения и самоопределения в условиях революции) и совместная 
  борьба с большевиками. Для казачьей верхушки принятие калмыков в войско было 
  единственной возможностью укрепить свои позиции в Нижнем Поволжье и в Юго-Восточном 
  Союзе. <br>
  <br>
  Декабрьский экстренный Войсковой Круг, констатировавший растущий конфликт с 
  неказачьим населением, угрозу казачьим землям и привилегиям, заявил о решимости 
  начать вооруженную борьбу с Астраханским ВРК, требующим ликвидации войска и 
  признания казаками Советской власти. Войсковое правительство приступило к подготовке 
  вооруженно- го выступления казаков и калмыков с целью уничтожения Советов в 
  Поволжье. Но, когда дело дошло до открытого столкновения, большинство казаков 
  отмежевалось от атамана и правительства, объявив о признании Советской власти 
  на условиях неприкосновенности казачьего уклада и хозяйства. <br>
  <br>
  Во время так называемого казачьего мятежа 11-24 января в Астрахани на стороне 
  войскового правительства выступил только полк стариков-добровольцев низовых 
  станиц, две роты астраханской офицерской организации, казачья батарея (переукомплектованная 
  офицерами), и около батальона «вольных казаков» - жителей Астрахани (в основном 
  - учащейся молодежи). Общая численность добровольческих частей восставших не 
  превышала 1500 человек. Калмыцкая часть войска к этому времени существовала 
  лишь на бумаге. После двух недель кровопролитных уличных боев восстание было 
  подавлено. 26 февраля 1918 г. в соответствии с решением «крестьянского съезда 
  Астраханской губернии от 15 января» и постановлением Астраханского губисполкома 
  от 20 февраля, по приказу председателя Астраханского ВРК есаула М.Л. Аристова, 
  Астраханское войско (учреждения и полки) было ликвидировано, а его население 
  «обращено в гражданское состояние». <br>
  <br>
        Таким образом, история казачьего автономизма для астраханцев, по большому 
        счету, ограничилась лишь кратким периодом революции 1917 г. Выбор казачьих 
        низов в пользу саморасказачивания в условиях революции и Гражданской войны 
        был неизбежным и закономерным, а любые попытки верхушки защитить особую 
        казачью политическую линию, сохранить казачью этно-социальную обособленность 
        были обречены на провал. Идея особой казачье-калмыцкой республики (на 
        определенном этапе поддержанная станичниками, исходя из необходимости 
        защиты собственных социально-экономических интересов) была отвергнута 
        большинством казаков и калмыков уже в начале 1918 г. <br>
