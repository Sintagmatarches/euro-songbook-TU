	<br><span class="text"><span class="header"><strong>БОЕВЫЕ РАСПИСАНИЯ ЮГО-ЗАПАДНОЙ И 
        ОТДЕЛЬНОЙ ОРЕНБУРГСКОЙ АРМИЙ</strong><br></span>
        <br>
       Вступительная статья, подготовка текста и комментарии А.В. 
        Ганина<br></em>
        <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 213-218</em>.<br>
        <br>
        <br>
        <em>Боевые расписания являются практически неисчерпаемым источником для 
        анализа проблем военного строительства и военной истории. Публикуемые 
        боевые расписания отражают процесс переформирования Юго-Западной армии 
        в Отдельную Оренбургскую. Юго-Западная армия была образована 17 октября 
        1918 года, главным образом, из частей Оренбургского казачьего войска и 
        действовала на левом фланге Восточного фронта белых. В первом из двух 
        публикуемых расписаний особый интерес представляет состав частей Уральского 
        участка Юго-Западной армии, как и сам факт существования такого участка 
        в подчинении командования этой армии. <br>
        <br>
        К сожалению, этот важнейший, на мой взгляд, вопрос не получил должного 
        освещения ни в советской, ни в современной российской историографии.<sup>1</sup> 
        Из документа следует, что в состав Уральского участка были включены практически 
        все части Уральского казачьего войска и Уральской армии, и даже Астраханский 
        пеший казачий дивизион. В итоге становится понятным смысл самого названия 
        Юго-Западной армии, как армии, объединявшей все антибольшевицкие силы 
        юго-западного (видимо, по отношению к Ставке в Уфе) направления. Однако 
        в одно время с Юго-Западной существовала и Уральская армия (известны приказы 
        по армии за 1918 г.),<sup>2</sup> обладавшая, по всей видимости, тактической 
        самостоятельностью. Штаб Юго-Западной армии осуществлял лишь общее руководство 
        операциями уральцев, что отражено в приказах по армии.<sup>3</sup> Подчинение 
        уральцев Дутову было чисто формальным, впрочем, таким же, как и их подчинение 
        Колчаку и Деникину, так как долгое время уральцы сражались отдельно от 
        своих союзников по антибольшевицкой борьбе.<sup>4</sup><br>
        <br>
        Боевое расписание Отдельной Оренбургской армии от 18 января 1919 г. уже 
        не содержит сведений об Уральском участке и уральских казачьих частях, 
        включенных в состав Отдельной Уральской армии (образована 28 декабря 1918 
        г. одновременно с Отдельной Оренбургской армией).<sup>5</sup> Вместе с 
        тем, появился новый участок фронта — Мрясовский, куда с Илецкого участка 
        были переброшены подразделения, ранее входившие в состав 2-й Оренбургской 
        казачьей дивизии. Значительные изменения произошли и на Бузулукском участке, 
        с которого в пользу Северного участка была целиком снята 5-я стрелковая 
        дивизия и 9-й Оренбургский казачий полк. За счет частей Орского участка 
        получил усиление Илецкий участок. Такие изменения были связаны с успехами 
        одновременного наступления 1-й и Туркестанской армий красных на Оренбург 
        с северо-запада и юго-востока, которое, в конечном итоге, привело к падению 
        Оренбурга 22 января 1919 г. <br>
        <br>
        Документы публикуются в соответствии с современными правилами орфографии 
        при сохранении стиля документа. Сокращения расшифрованы в квадратных скобках. 
        Содержание последней графы каждого расписания — «ПРИМЕЧАНИЕ.» — вынесено 
        в сноски в конце публикации. В квадратных скобках приведены уточненные 
        итоговые данные по численности войск. <br>
