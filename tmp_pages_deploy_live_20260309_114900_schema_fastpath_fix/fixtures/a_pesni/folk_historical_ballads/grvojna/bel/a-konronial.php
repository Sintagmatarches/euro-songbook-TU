	<span class="text"><strong>Р.П. Чубарева-Завольская,  директор музея истории г. Оренбурга<br>
	</strong>
        <br>
        <span class="header"><strong>КОНЬ РОНЯЛ СЛЕЗУ... </strong><br></span>
        <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 212-213</em>.<br>
        <br>
        <br>
        <em>С высоты времени стало понятно, кому и для чего нужна была октябрьская 
        бойня. В водоворот событий страну ввергли люди с амбициями фараонов... 
        Только те, кто никогда не вырастил дерева, никогда не засеял пшеничное 
        поле, мог рушить ради власти весь мир. Огнем и мечом прошла революция 
        по стране. Один из участников говаривал: «Есть у революции начало, нет 
        у революции конца». А ведь так и есть: гражданская война сразу после революции, 
        вторая мировая война, события в Румынии, Чехословакии, на Кубе, потом 
        Афганистан, Чечня... Воистину, нет у революции конца! </em><br>
        <br>
        ...В окно постучали кнутом. Ванечка встрепенулся: это тятя!.. Встревожились 
        все: бабушка Евдокия, внучки Евгения и Груня. Позвали хозяина — Якова. 
        Долго он не возвращался. Потихоньку домочадцы стали выходить во двор. 
        Запричитала бабушка... на попоне в телеге лежал ее сын Василий — молодой 
        казак. Убили красные. Новое слово... жуткое слово. <br>
        <br>
        Ванечка остался без отца, а вскоре умерла и мать. <br>
        <br>
        Груня (моя мать), а было ей лет восемь, запомнила одну деталь — конь склонился 
        над лицом хозяина, а из огромного глаза катилась слеза. Когда она об этом 
        говорила, сама всегда плакала. <br>
        <br>
        Синеглазый, с черными, как смоль кудрями и румянцем во все щеки казак 
        Василий был участником Первой мировой войны, как и все его братья: Яков 
        (мой дед), Степан, Кузьма, Антон (двоюродный брат деда). <br>
        <br>
        У всех трагическая судьба. Степан умер от тифа сразу после войны, Василий 
        убит красными, Яков — коваль казачьего полка — стал колхозным конюхом, 
        Антон — председателем колхоза после репрессированного первого председателя 
        Марка Сафоновича Фоменко, а третьим был сын Степана - Федор — участник 
        уже Второй мировой войны. <br>
        <br>
        Мало мы знаем об истинной истории — не рассказывали родные, боясь обвинений 
        за казацкое прошлое. Были спрятаны шашки, фотографии и даже в биографии 
        казачье прошлое упускали. Только сейчас по крупицам воссоздают картину 
        Гражданской войны, коммуны (1929 г.) и коллективизации в моей родной деревне 
        — Верхней Кардаиловке Кваркенского района Оренбургской области. Моими 
        собеседницами бывали при встречах тетки Евгения, Екатерина, Анна - дочери 
        братьев Якова, Степана, Григория. Три сестры ушли в мир иной в один 2001 
        г., друг за дружкой: мама в 90 лет, Екатерина в 80, Анна в 70 лет. Теперь 
        некого спросить — ушло поколение свидетелей тех непростых, полных горя 
        и лишений лет. <br>
        <br>
        ...Василия Семеновича похоронили у церкви. Советская власть еще не нагрянула 
        в деревню. Деревянная церковь стояла в центре села. Далеко был виден купол 
        из поймы Урала, куда бегали мы за кисляткой (щавелем), клубникой, цветами. 
        В один из моих приездов в 1993 г. тетя Евгения рассказала о Василии Семеновиче 
        и говорит: «А ты пойди к церкви, там колышек от креста!..». Боже мой, 
        никто ни разу мне не сказал, что он похоронен у церкви, мимо которой я 
        бегала в школу. И вот теперь, когда от церкви осталась груда кирпича от 
        фундамента, когда она сгорела уже в 1970-х, узнала такую историю. У церкви 
        всегда хоронили достойных. В советское время в церкви был клуб, потом 
        зернохранилище, от могил ничего не осталось. Мы их затоптали. И колышка 
        не осталось!.. <br>
        <br>
        ...На горе недалеко от школы стоит с 1933 г. памятник, обнесенный железной 
        оградкой. Здесь покоится прах красного партизана Семена Григорьевича Кравцова, 
        двоюродного брата моего деда. <br>
        <br>
        Так вот расходились пути-дорожки казаков. Семен Григорьевич воевал против 
        Дутова, получил два ордена, был направлен в Оренбург на курсы красных 
        командиров. Воевал на Туркестанском фронте, затем в Сибири, был ранен. 
        Уже тяжелобольным туберкулезом вернулся домой и был похоронен у дороги, 
        ведущей в село. Такая вот история. Смерть сравняла всех: и белых, и красных. 
        <br>
        <br>
        Прабабушка Евдокия прожила около ста лет. Уже слепую я водила ее на погост 
        через ковыльную степь. Она голосила за сыном Степаном и внуком Николаем, 
        умершим в 1939 г. после действительной службы в Красной армии от простуды. 
        О том, что у церкви похоронен другой ее сын, она тоже не говорила: боялась 
        — он был белым! <br>
        <br>
        Пришла перестройка, и мы кое о чем узнали правду, хоть и поздно, да и 
        то не всю. <br>
        <br>
        Молодежь часто и не понимает — почему даже есть такой государственный 
        праздник — примирение? Им невдомек, что мириться надо тем, кто случайно 
        оказался с красными или белыми в этой неразберихе, называемой революцией. 
        Кто знал, где правда? <br>
        <br>
        Думаю, лучше всего знали это труженики полей — как мой дед Яков Семенович, 
        казак и хлебопашец: «Вот бисевы диты воюють. Надо хлиб сеять, робыть надо, 
        а воны то били, то красни, то ще зэлэны» (были ведь и такие!). Забирал 
        коней (чтобы не взяли в обоз) и уезжал на речку Гусиху, где была его земля, 
        на которой росли пшеница, овес, горох. Трудился не покладая рук, того 
        требовал и от детей. Возили излишки на ярмарку, покупали нужный товар. 
        Хоть и не богато жили, но не голодали, как в советское время в 20-30-х, 
        да и 40-х гг.! <br>
        <br>
        О том, что никто не понимал, зачем и с кем нужно воевать, говорит такой 
        эпизод. 1918 г. Наступали то красные, то белые со стороны Челябы. В селе 
        женщины сидели по домам, а дети а подполе. Пить, есть надо, кончились 
        запасы воды — брали ее с Урала. Кому идти? Пошли старая бабка да малая 
        Евгения. А на реке наступление, пушки палят, снаряд ударил в скалу. Преодолевая 
        страх, набрали воды и увидели бегущих красноармейцев. Возвращаются, а 
        бабка кричит снохам: «Жинки, жинки, бачили мы тех красних, воны такие 
        же люды, як мы, тильки балакают по-русськи!» <br>
        <br>
        А спросили бы тогда казака любого — нужна ли была им революция, потом 
        коллективизация? Ведь они работали на себя. А в колхозе от зари до зари 
        за палочки - трудодни вообще не платили. А жили на то, что сажали картошку 
        да огород. 10 часов на государство, а остальное — на себя. Так как им 
        было лучше? А людям навязывали то революцию, то Гражданскую войну, когда 
        брат пошел на брата. <br>
        <br>
        В Кардаиловке устроили коммуну, обобществили скот, устроили в церкви столовую 
        — не сыт, не голоден, дома шаром покати. Потом продразверстка: выметали 
        у сельчан все сусеки до пылинки. Участвовал в этой кампании и мой дядя. 
        Бабушка Анисья говорила: «Сынок, что вы робите, ведь весной люди с голоду 
        будут умирать!» он отвечал: «Мама, так надо!» а весной и начался страшный 
        голод, ели лебеду, корни, пухли, умирали. Сеять было нечем, потому и следующий 
        год тоже был голодным. Сам продразверстчик просил сестру: «Дай гороху! 
        Я знаю, мама спрятала мешок». Сестра отсыпала тайком из кармана. Горох 
        готовили так: варили, потом жарили — жуешь по горошине и голод не чувствуешь. 
        Вот так грандиозные дела коммунистов оборачивались бедствием народным. 
        <br>
        <br>
        Потом коллективизация началась. Загоняли в колхоз. Так и говорили сельчане: 
        «Когда нас загнали в коммуну», или «Когда сгоняли в колхоз» деваться некуда 
        - шли. Прятали фуражки, шашки, знаки отличия. Боялись слова «казак». Дед 
        припрятал шашку на чердак, а подросшие внуки нашли и на заднем дворе стали 
        рубить бурьян: вжик-вжик. Дед обезумел, увидев рубак. Отобрал шашку и 
        переломил через колено: «Нэма казакив!» из обломков шашки сделал два ножа, 
        загнутой частью резал из кожи вожжи. <br>
        <br>
        Некогда богатое село с добротными деревянными домами стало хиреть, но 
        особенно подкосило его освоение целины, когда перенесли центральную усадьбу 
        ниже по Уралу. Сейчас главная улица представляет жалкое зрелище: дома 
        без окон, или на месте некогда красивых домов с резными ставнями, крылечками 
        — груда золы, камней и кирпича. <br>
        <br>
        Раскулачивание в нашей Кардаиловке — это еще одна горькая страница и другая 
        история. О ней надо говорить отдельно. <br>
        <br>
        Оренбург, декабрь 2002 г. <br>
