  <br>
  Комиссия поехала в Ростов, там... был Белобородов<sup>8</sup>, мнение которого 
  по земельному вопросу: «Единственно правильной мерой для Северного Кавказа вообще 
  и Горской республики в частности является оккупация этого района советскими 
  войсками. Ни ориентация на казаков, ни ориентация на горцев пользы принести 
  не может». <br>
  <br>
  Комиссия продовольствия заявила: «Выселение казаков из пределов ГАССР с продовольственной 
  точки зрения будет большой ошибкой...» <br>
  <br>
  Постановление Президиума ВЦИК о приостановке переселения казачества было большой 
  неожиданностью для Кирова, тем не менее, 19 февраля за моей и Кирова подписью 
  была дана депеша Облисполкому и парткому г. Владикавказа, отдельскому и окрисполкому 
  и парткому г. Грозного, казачьему ревкому... <br>
  <br>
