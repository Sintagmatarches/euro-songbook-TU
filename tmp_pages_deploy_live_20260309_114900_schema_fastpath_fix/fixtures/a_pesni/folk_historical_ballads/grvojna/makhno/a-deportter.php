  <br>
<span class="header"><strong>К ВОПРОСУ О ДЕПОРТАЦИИ ТЕРСКОГО КАЗАЧЕСТВА В 1918-1920 
  гг.</strong><br></span>
  <br>
  <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. М., «Посев», 
  2005, стр. 130-153.</em><br>
  <br>
  <br>
  Одной из самых драматических страниц истории народов Северного Кавказа является 
  переселение терского казачества, начатое Советской властью в 1918 г. Прерванное 
  пребыванием на Северном Кавказе армий А.И. Деникина (февраль 1919 — март 1920 
  гг.) оно продолжилось до начала декабря 1920 г. 27 января 1921 г. Президиум 
  ВЦИК РСФСР постановил «немедленно приостановить» выселение казаков из пределов 
  Горской АССР, образованной к тому времени на территории бывшей Терской области, 
  а 14 июля 1921 г. Президиум ВЦИК запретил переселение, назвав его ошибочной 
  мерой. К тому времени примерно 25 тысяч терских казаков, примерно 1/10 казачьего 
  населения Терека покинула свои станицы. <br>
  <br>
  Многие современные исследователи называют выселение терских казаков 1918-1920 
  гг. первой в России депортацией. Слово «депортация» означает изгнание или ссылку 
  как меру уголовного или административного наказания. Поэтому, строго говоря, 
  депортацией можно назвать выселение терских казаков в ноябре — декабре 1920 
  г., лишь из 5 станиц за то, что они участвовали в октябре 1920 г. в восстание 
  против Советской власти. <br>
  <br>
  Что же касается переселения 3-х терских станиц весной 1918 г. и вторичного выселения 
  тех же станиц весной 1920 г. (Деникин вернул выселенных в 1918 г. казаков в 
  их станицы), то вряд ли можно считать это переселение депортацией: это была 
  попытка Советской власти провести «черный передел» в Терской области, решить, 
  как она предполагала земельный вопрос «по справедливости». <br>
  <br>
  Анализ документов по истории переселения терских казаков 1918-1920 гг. никак 
  не позволяет увидеть в нем проявление «русофобии», как утверждает И. Пыхалов.<sup>1</sup> 
  Большевики не боролись «c русскими». Они действительно хотели с помощью «безземельно 
  переселения» не всех, а части казаков обеспечить землей безземельных и малоземельных 
  и горцев, и русских иногородних и казаков (здесь и далее в скобках ссылки на 
  документы в приложении документы №№ 1, 2, 3, 4, 5).<br>
  <br>
  И. Пыхалов сомневается в наличии земельного голода у горцев Терской области. 
  Между тем, специальная комиссия, созданная в 1906 г., для обследования земельного 
  фонда на Северном Кавказе, пришла к выводу о том, земельный голод у горцев был 
  вопиющим. Терское казачество, составлявшее до революции немногим более 20% населения 
  области, владело более чем 50% удобной (30% всей) земли. Земельный казачий пай 
  на Тереке (в среднем) составлял 18,8 десятин земли, а на одну душу горца приходилось 
  0,57 десятин пашни. Комиссия установила, что средний земельный пай горца мог 
  прокормить только 14% проживающего в горах населения, остальные должны были 
  переселиться оттуда или медленно вымирать от голода и болезней — они были «лишними 
  ртами», «избыточным населением». <br>
  <br>
  О земельном голоде горцев говорили на заседаниях Государственной Думы в 1906, 
  1907 гг. избранные от Терской области А.П. Маслов и Т.Э. Эльдарханов. <br>
  <br>
  Это тогда было сказано, что у чеченца столько земли, сколько помещается под 
  его буркой, а кусок земли под одной коровой стоит столько, сколько одна корова.<sup>2</sup> 
  <br>
  <br>
  Не удивительно, что горцы, которые то и дело «выходили за рамки законности» 
  в борьбе за землю, торопили Советскую власть с решением это вопроса. Один из 
  чеченских революционеров А. Шерипов говорил на съезде народов Терской области 
  в мае 1918 г.: «Мы загнаны в суровые ущелья и дикие скалы гор и лишены возможности 
  всякого человеческого существования. Если наши справедливые требования не получат 
  удовлетворения, то вся голодная армия безземельных масс может стать страшной 
  силой в руках контрреволюции. У нас нет Родины, потому что нет земли. Дайте 
  нам родину, и вы встретите в нас братьев, рядом с вами сражающихся против всех 
  врагов революции».<sup>3</sup><br>
  <br>
  Земельный вопрос, который был «альфой и омегой», вопросом жизни и смерти, который 
  всегда «делал политику Терской области» обсуждался на съездах народов Терека 
  в 1918 г. (до прихода в Терскую область Деникина их прошло пять). Это съезды 
  нельзя назвать, как это делает П. Полян,<sup>4</sup> съездами Советов: на съездах 
  были представлены, кроме Советов, городские Думы, сельские самоуправления, военно-революционные 
  комитеты, делегаты, избранные на сельских и станичных сходах.<sup>5</sup><br>
  <br>
  4 марта 1918 г. II съезд народов Терека в Пятигорске (с 5 марта он переехал 
  во Владикавказ) признал власть СНК РСФСР и провозгласил образование на территории 
  Терской области Терской народной республики, объявив ее составной частью РСФСР, 
  принял ее конституцию, создал высшие органы — Терский народный совет и СНК. 
  <br>
  <br>
  По конституции Терской республики в национальных округах и казачьих отделах 
  органами власти объявлялись не Советы рабочих, крестьянских, казачьих и горских 
  депутатов, а народные советы и их исполкомы, в которых имели право избирать 
  и избираться имущие классы. <br>
  <br>
  На II съезде народов Терека горские делегаты выступили с требованием вернуть 
  их исконные земли, что вызвало недовольство казачества. После того, как съезд 
  проголосовал за решение III Всероссийского съезда Советов о социализации земли, 
  часть казачьих делегатов ушла со съезда, заявив что только Учредительное собрание 
  имеет право решать земельный вопрос. Никаких конкретных мер по практическому 
  осуществлению советских земельных законов съезд не принял. <br>
  <br>
  III съезд народов Терека (22-28 мая 1918 г., Грозный), подтвердив решения предыдущего 
  съезда о конфискации помещичьих и крупных частновладельческих земель, отменил 
  частную собственность на землю, запретил куплю-продажу земли с тем, чтобы наделить, 
  прежде всего, безземельных горцев и беднейшее казачество. <br>
  <br>
  На такие решения, безусловно, повлияла и сложнейшая политическая обстановка 
  в области, и наступление турецкой армии на Кавказе, и выступление горских фракций, 
  в том числе и А. Шерипова, который заявил, что «от способов решения земельного 
  вопроса» зависит «исход революции в Терской области». На крики казачьих депутатов 
  на съезде: «Не боимся, не запугаете! Это ультиматум!» Шерипов отвечал: «Мы сказали 
  вам объективно о настроении наших масс, никакого ультиматума с нашей стороны 
  не было».<sup>6</sup><br>
  <br>
  Для наделения безземельных (в резолюции говорилось, что в «в первую очередь 
  удовлетворяются безземельные горские жители Ингушетии, Осетии, Чечни, Башкирии 
  и иногородние»). III съезд постановил образовать запасной земельный фонд. Резолюция 
  называет 3 источника его формирования: 1) земли, полученные «путем уничтожении 
  чересполосицы», 2) избытки «после распределения земли по установленной уравнительной 
  мере»; 3) частновладельческие земли. Заметим, что уничтожению чересполосицы 
  в резолюции отведено первое место. <br>
  <br>
  Уничтожить чересполосицу, т.е. выселить казачьи станицы, клином вдавшиеся в 
  горские земли, предполагалось путем «безболезненного переселения» казаков на 
  новые земли, а горцев в казачьи станицы «чтобы установить наконец, мир между 
  ними». <br>
  <br>
  За резолюцию по земельному вопросу («немедленно приступить к урегулированию 
  чересполосицы путем переселения для уравнения национальных границ применительно 
  к закону о земле») проголосовало 303 делегата, 127 воздержалось, ни одного голоса 
  против не было. <br>
  <br>
  Съезд постановил, что «переселение должно производиться так, чтобы переселяемый 
  попадал по возможности в однородные с прежними привычными ему сельскохозяйственными 
  и климатическими условиями». <br>
  <br>
  Для обустройства на новом месте казакам должна быть выдана беспроцентная ссуда. 
  За оставляемое ими в станицах имущество те, кто вселялся в станицы, должен был 
  заплатить «по справедливой оценке». Вопрос о времени и способе удовлетворения 
  выселяемых решали органы народной власти. <br>
  <br>
  Территория, куда должны были переселять станицы, отводилась заранее и там «создавались 
  условия для переселения». До переселения власть и соседствующие с предполагаемыми 
  к переселению станицами горцы должны были принять все меры «к ограждению мирной 
  жизни и имущества названных станиц». <br>
  <br>
  Резолюция назвала проведение земельной реформы первоочередной задачей Терского 
  народного Совета и СНК Терской республики, объяснив причины этого: «ввиду серьезности 
  момента и зависимости стойкости общего фронта защиты области от решения земельного 
  вопроса». <br>
  <br>
  Съезд наметил к выселению 4 станицы: Тарскую, Сунженскую, Воронцово-Дашковскую 
  (она же - Аки-юрт, Акки-юрт, или Акхи-юрт), Фельдмаршальскую с тем, чтобы их 
  земли передали ингушам.<sup>7</sup> Следует отметить неточность, допущенную 
  П. Поляном<sup>8</sup>: станица Сунженская не была переименована в станицу Аки-юртовскую, 
  как утверждает исследователь. Переименована в Аки-юртовскую была станица Воронцово-Дашковская. 
  <br>
  <br>
  О том что, «безболезненного переселения» казаков в 1918 г. не вышло, свидетельствует 
  доклад делегации казаков и крестьян Терской области во ВЦИК 22 декабря 1920 
  г. (документ №26). Оказывается, при выселении станицы Тарской казаки вместе 
  с осетинами (осетины в тот период были против выселения казаков) отбивались 
  от ингушей и красноармейцев, «многие мужчины расстреляны», - писали казаки в 
  1920 г. <br>
  <br>
  Доклад председателя комиссии ВЦИК по наделению безземельных горцев землей во 
  ВЦИК в феврале 1921 г. подтверждает, что при выселении станицы Тарской (в ней 
  проживало 6 тысяч человек) было убито 57 мужчин и 11 женщин (документ №29). 
  <br>
  <br>
  Эти и другие документы свидетельствуют, что в 1918 г. были выселены 3 станицы: 
  Тарская с хутором, Сунженская и Аки-юрт. Что касается станицы Фельдмаршальской, 
  предназначенной к переселению и станицы Кохановской, то они, по словам казаков, 
  «были сожжены ингушами» в конце 1917 г., «скот угнан ими, а казаки скитались 
  по соседним станицам». <br>
  <br>
  Необходимо пояснить, что в 1918 г. осетины были против выселения казаков. 5 
  декабря 1918 г. на V съезде народов Терека делегат осетинской фракции С.А. Такоев 
  заявил: «Несомненный факт, что горцы малоземельны, но разве для того, чтобы 
  наделить их землей, необходимо лишить земли других трудовых землеробов?... Осетинская 
  фракция поручила мне заявит следующее: ...земельный вопрос мы должны решить 
  в интересах всех трудящихся элементов. Чем же виновато трудовое казачье население, 
  что его, хотя бы и в стратегических целях, поместили здесь? Я полагаю, что выселением 
  казачьих станиц мы добьемся не пролетарского решения вопроса, разрешения его 
  не на трудовых началах, а на буржуазных». С.А. Такоев, который был председателем 
  II и III съездов народов Терека в 1918 г., призывал к единению: «Мы все — и 
  горцы, и казаки — одна трудовая семья, и нам необходимо жить в мире и братстве. 
  Но, уничтожая чересполосицу, мы снова подойдем к гражданской войне».<sup>9</sup><br>
  <br>
  Н.Ф. Бугай и А.М. Тонов явно ошибочно приписали эту речь С.А. Такоева Г.К. Орджоникидзе, 
  из чего последовал их же ошибочный вывод о якобы неоднозначной позиции Орджоникидзе 
  по вопросу о переселении станиц и о его сомнениях в правильности этой меры.<sup>10</sup><br>
  <br>
        Факт помощи осетин Тарским казакам при их выселении в 1918 г. подтвердил 
        25 сентября 1920 г. в своем письме в Наркомнац член Осетинского ревкома. 
        В 1920 г. и Осетия стояла за «уничтожение казачьего клина» (документ №11).<br>
  <br>
  К началу V съезда народов Терека (ноябрь 1918 г.) три станицы были выселены, 
  а ингуши были «удовлетворены в отношении земельного фонда», как с удовлетворением 
  отметил в своем выступление на V съезде представитель горцев Цаликов. В то же 
  время, акция выселения казаков вооруженным путем удручающе подействовала на 
  делегатов съезда. Тот же Цаликов даже выразил сочувствие казакам: «я знаю, что 
  трудно перейти из своего гнезда, где человек жил десятки лет, на другое новое 
  место». Он осудил способ переселения казаков: «Тот метод, по которому недавно 
  был разрешен этот вопрос, то есть с оружием в руках, нельзя принять...» <br>
  <br>
  Резко выступила на V съезде народов Терека против выселения других казачьих 
  станиц, граничивших с горскими землями, фракция левых эсеров. Она назвала «незакономерным 
  полное уничтожение чересполосицы и связанное с этим выселение казачье-крестьянского 
  элемента из давно насиженных мест». <br>
  <br>
  Возражения против переселения казаков осетинской фракции и левых эсеров, не 
  говоря уже о протестах казаков, заставили большевицкую фракцию несколько отступить 
  от своей прежней позиции. Большевики предложили считать, что «разрешение земельного 
  вопроса в области отнюдь не стоит в плоскости исключительной необходимости переселения 
  аулов, станиц и селений, а стоит в плоскости равномерного распределения всего 
  областного фонда между трудящимися всех народов области». <br>
  <br>
  Эти предложения большевиков V съезд принял в качестве резолюции с добавлением 
  слов: «... не исключая частичного устранения чересполосицы по мере надобности 
  и с соглашения заинтересованных сторон». Таким образом, уничтожение чересполосицы 
  уже не являлось основным источником формирования областного земельного фонда, 
  хотя оно все еще «не исключалось», но применять его намеревались лишь «по мере 
  надобности» и «с согласия сторон». <br>
  <br>
  V съезд решил «немедленно наделить безземельных и малоземельных горцев, крестьян 
  и казаков землей из областного фонда», а, вместе с тем, и «все трудовое население 
  выселенных станиц Тарской, Сунженской и Аки-юрт», и «беженцев разоренных слобод 
  и хуторов». Последним двум категориям населения в родные станицы возвращаться 
  не разрешалось. Земля им отводилась «в местах, определяемых областной властью».<br>
  <br>
  Для изыскания недостающих земель V съезд решил, не переселяя пока больше других 
  станиц, отрезать у них часть земель и передать их «трудящимся ингушам, чеченцам, 
  осетинам».<sup>11</sup><br>
  <br>
  Из 70 казачьих станиц Терской области, в которых проживало около 280 тысяч человек, 
  Советская власть намеревалась переселить 18 с населением в 60 тысяч человек.<sup>12</sup> 
  (документ № 14, 26). До прихода в область армии Деникина успели выселить 3 станицы. 
  Результатом начавшихся переселений и «черного передела» земель пока остававшихся 
  на месте станиц было то, что горская беднота защищала Советскую власть, большевиков 
  от деникинцев, как говорит А. Авторханов, «не по обычаям горского гостеприимства, 
  а по соображениям классового порядка».<sup>13</sup><br>
  <br>
  В то же время, аграрная политика большевиков в 1918 г. привела к их вооруженному 
  конфликту с казачеством и, во многом, способствовала падению Советской власти 
  в области. А.И. Деникин, армия которого заняла Терскую область в феврале 1919 
  г., возвращал казаков в их станицы (документ №26), поэтому после ухода в марте 
  1920 деникинцев с Северного Кавказа выселенные в 1918 г. станицы пришлось выселять 
  вторично. <br>
  <br>
  Горцы восторженно встречали вступивших в пределы Терской области Красную армию 
  и первых представителей Советской власти (документ №1), которые сообщали, что 
  «настроение горцев всецело на стороне Советской власти». <br>
  <br>
  Осознавая, что она «далеко не так влиятельна на Кавказе, как это необходимо, 
  что бы держаться изнутри» (документ №2), Советская власть продолжила свою прежнюю 
  аграрную политику: вновь выселила станицы Тарскую, Сунженскую, Аки-юрт, а примерно 
  в конце июля Терский областной ревком распорядился выселить еще несколько станиц 
  Сунженской линии (документ №6). <br>
  <br>
  Американский историк Г. Дерлугьян почему-то находит союз «чеченских исламистов 
  и северо-кавказских большевиков», сложившийся во время Гражданской войны, «странным», 
  хотя сам признает, что» значительная часть чеченцев готова признать советскую 
  власть на идейном уровне».<sup>14</sup><br>
  <br>
  Горцам прежде всего близка была политика большевиков по возвращению им их бывших 
  земель. Во многом, именно поэтому горцы поддержали Советскую власть. В период 
  Гражданской войны даже часть духовенства, так называемые «бедные муллы», вместе 
  с горской беднотой боролись за нее, даже проявляя желание «записаться в коммунистическую 
  партию». В своем выступлении в с. Назрани 16 марта 1924 г. мулла ингушского 
  аула Шалги Магомет Мистоев с гордостью сказал, что «21 ингушский мулла пал в 
  борьбе за советскую власть».<sup>15</sup><br>
  <br>
  Намереваясь обустроить все народы Северного Кавказа, Советская власть не смогла 
  защитить интересы казачества, которое не могло не противиться крестьянской революции 
  в деревне, когда весной 1918 г. беднейшее крестьтянство поставило вопрос об 
  уравнительном переделе земли, который в условиях Северного Кавказа сразу же 
  поворачивался против казачества, в том числе против его средних слоев, как наиболее 
  массового земельного собственника. С весны 1918 г. «на волне возникшей тревоги 
  за судьбу сословных привилегий заполыхали антисоветские мятежи».<sup>16</sup> 
  В том, что казаки довольно долгое время оставались «вандейскими войсками» и 
  после революции, немалую роль сыграла политика Советской власти по отношению 
  к ним, которая была весьма противоречива. <br>
  <br>
  После установления своей власти большевики стремились «изжить» этот феномен, 
  это своеобразное «войско-сословие», «народ-помещик», и, в то же время, прекрасно 
  отдавали себе отчет в том, что казачество не сплошная контрреволюционная масса, 
  что среди него есть и союзник пролетариата — бедняк, и колеблющийся середняк, 
  которых надо оторвать от верхов казачества и привлечь на свою сторону. Результаты 
  активной политики Советской власти по расколу казачьей массы и привлечения на 
  свою сторону ее бедняцких и середняцких слоев изучены историками вполне достаточно, 
  хотя дискуссии о том, «когда и насколько» бедняки и середняки казачества «отошли 
  от лагеря контрреволюции» и заняли «политику нейтралитета» или «поддержали советскую 
  власть» вряд ли, когда-нибудь закончатся. <br>
  <br>
  А.И. Козлов справедливо считает «далеко не бесспорным» (в отношении казачества 
  Дона, Кубани и Терека) положение о том, что: <br>
  <br>
  1) основная масса казачества перешла на сторону Советской власти с конца 1918 
  по январь 1919 гг.; <br>
  <br>
  2) основная масса середняцкого казачества с лета 1918 по осень 1919 гг. была 
  нейтральной; <br>
  <br>
  3) в ноябре—декабре 1919 г. в рядах РККА казаков было уже больше, чем у белых. 
  <br>
  <br>
  Помимо довольно взвешенных мер, проводившихся Советской властью в отношении 
  казачества, выразившихся в принятии пятого пункта декрета о земле («земля рядовых 
  крестьян и рядовых казаков не конфискуется»); «Обращения II Всероссийского съезда 
  Советов к казакам» («вам говорят, что Советы хотят отнять у казаков землю. Это 
  ложь. Только у казаков-помещиков революция отнимет земли и передаст их народу»); 
  воззвания ВЦИК к казакам от 1(14) ноября 1917 г. («Казаки! Переходите на сторону 
  победившего народа!»); обращения СНК от 25 ноября (8 декабря) 1918 г. «Ко всему 
  трудовому населению» о борьбе с восстаниями Каледина и Дутова (контрреволюционеры 
  «превращают трудовое казачество в орудие для своих преступных целей»); обращения 
  подписанного В.И. Лениным от 9(22) декабря 1917 г. «ко всему трудовому казачеству» 
  с постановлением об отмене воинской повинности и других льготах казакам.<sup>17</sup> 
  Помимо этих, довольно взвешенных мер, были и другие. <br>
  <br>
  В директиве Оргбюро ЦК РКП(б) от 24 января 1919 г. наряду с задачами нейтрализации 
  среднего казачества, главный упор делался на репрессивные меры. Это циркулярное 
  письмо к местным партийным организациям признавало «единственно правильным самую 
  беспощадную борьбу со всеми верхами казачества путем поголовного их истребления 
  — никакие компромиссы, никакая половинчатость недопустимы».<sup>18</sup> Ответом 
  на директиву было восстание донских казаков. 16 марта того же 1919 г. Пленум 
  ЦК РКП(б) вынужден был приостановить действие директивы. <br>
  <br>
  По отношению ко всем казакам без разбора проводились массовые репрессии, местные 
  власти издавали приказы об упразднении названий «станица», «казак», о запрете 
  казакам носить лампасы; были реквизиции, в том числе и у трудового казачества, 
  конской упряжи с телегами, отменялись местные ярмарки и прочее. Ленину пришлось 
  призвать РВС Кавказского фронта телеграммой от 3 июня 1919 г. «быть особенно 
  осторожным в ломке таких бытовых мелочей», даже «делать поблажки в привычных 
  населению архаических пережитках», поскольку они в общей политике «совершенно 
  не имеют значения», а «ломка» их только «раздражает население».<sup>19</sup><br>
  <br>
  После освобождения в марте 1920 г. Северного Кавказа от деникинской армии у 
  Советской власти не было сомнений в том, что ее опорой здесь являются горцы, 
  подавляющую часть которых составляли бедняки. По отношению к казачеству такой 
  твердой и однозначной позиции у центральной и местной власти, похоже, не было 
  (документ №2).<br>
  <br>
  27 апреля 1920 г. Северо-Кавказский ревком намеревался для стабилизации обстановки 
  на Тереке разоружить и казаков, и пограничные с казачьими станицами горские 
  аулы. Г.К. Орджоникидзе поднял на этом заседании ревкома вопрос о разоружении 
  горцев снять с повестки дня: «... несмотря на разбойничий дух, они являются 
  угнетенным элементом на Северном Кавказе и единственно верным оплотом Советской 
  власти», — заявил он.<sup>20</sup><br>
  <br>
  В мае 1920 г. на совещании казаков в Пятигорске Орджоникидзе на жалобы казаков 
  о грабежах их чеченцами и ингушами ответил, что для урегулирования взаимоотношений 
  русского населения с горцами необходимо устранить чересполосицу путем переселения 
  всех станиц, территориально прилегающих к горскому населению.<sup>21</sup><br>
  <br>
  1 июня 1920 г. член Северо-Кавказского ревкома Мдивани повторил эту мысль на 
  заседании ревкома (документ №4). 28 сентября 1920 г. С.М. Киров после продолжительной 
  беседы с В.И. Лениным сообщил Оржоникидзе о ленинской позиции по вопросу о политике 
  Советской власти на Северном Кавказе: «Общая линия поведения — опора на горцев. 
  Землю надо дать чеченцам».<sup>22</sup><br>
  <br>
  Вместе с тем, он, вероятно, еще в июне 1920 г. пришел к выводу о необходимости 
  отмены выселения терских казаков, поскольку в конце июня подписал телеграмму 
  владикавказскому ревкому с предписанием немедленно приостановить принудительное 
  расселение казаков станиц Тарской, Сунженской, Воронцовско-Дашковской и отчуждения 
  у них пахотных земель. Он ориентировал работников, посланных на Северный Кавказ 
  из центра, на поиски других путей решения вопроса наделения горцев землей. <br>
  <br>
  Биографическая хроника Ленина показывает, как напряженно работал он над изучением 
  казачьего вопроса в эти месяцы, выслушивал выступления казачьих делегатов на 
  съезде трудовых казаков в Москве в марте 1920 г., сам выступал на нем, сопоставлял 
  различные точки зрения, обсуждал казачий вопрос в разных инстанциях — и только 
  19 октября 1920 г. подписал декрет о землепользовании и землеустройстве в бывших 
  казачьих областях для внесения его во ВЦИК.<sup>23</sup> 18 ноября 1920 г. декрет 
  был узаконен. Он фактически не затронул казачье землепользование. <br>
  <br>
  Вполне обоснованно мнение В.В. Щетнева о том, что в политической ситуации конца 
  1920 г. идти на ломку казачьего землевладения было нецелесообразно — в то время 
  была необходима осторожная земельная политика.<sup>24</sup><br>
  <br>
  Многочисленные источники не оставляют сомнений в том, что восстание терских 
  и сунженских станиц в октябре 1920 г. было, в основном, вызвано переселенческой 
  политикой Советской власти, против отчуждения части казачьих земель казаки не 
  возражали. Накануне восстания станицы (до объявления об их выселении) не было 
  настроены антисоветски: они «дали своих самомобилизовавшихся людей в красную 
  армию» при проведении «Недели против Врангеля»; законопослушно просили Терский 
  облревком (исполком ст. Закан-Юрт, 1 августа 1920 г.) «разрешить ходатайствовать 
  перед ВЦИК» об отмене распоряжения о выселении станиц Сунженской линии; проводили 
  добровольный сбор продовольствия для Красной армии; станицы Нестеровская, Михайловская 
  даже «самооборонялись» от восставших (документ № 6, 7, 9, 12, 22). К. Еремеев 
  в письме в ЦК РКП(б) 19 июля 1921 г. писал: «В натуре «контрреволюционных казачьих 
  станиц» не существует... Контрреволюционность станиц вообще, огулом, - вредная 
  легенда» (документ №35) Это мнение подтверждает доклад И.В. Кошера в РВС Кавказского 
  фронта от 8 декабря 1920 г.: из 13 350 человек, выселенных из четырех сунженских 
  станиц Советскую власть поддерживали 6246 станичников (документ №24). <br>
  <br>
  Выселение четырех сунженских (Ермоловской, Закан-Юрт, Самашкинской и Михайловской) 
  и одной терской станицы — Калиновской (тотально всего населения) проведено было 
  по приказу Орджоникидзе и Сталина (документ № 18, 24, 25). Орджоникидзе лично 
  приказал «наиболее злостную» станицу Калиновскую сжечь (документ № 19, 43). 
  (позднее он отменил это решение). Таким образом, выселяя эти станицы, руководствовались 
  уже не решениями Съездов народов Терека 1918 г., а приказом РВС Кавказского 
  фронта от 29 июля 1920 г. о «приведение в повиновение» станиц, восставших против 
  Советской власти, «самыми решительными мерами вплоть до их разорения и уничтожения».<sup>25</sup><br>
  <br>
  Кавказское бюро ЦК РКП(б) 26 октября 1920 г. «одобрило и санкционировало все 
  мероприятия Орджоникидзе по подавлению восстания сунженских казаков» (документ 
  № 13). <br>
  <br>
  Выселено было из Терской области не 70 тысяч казаков, как считают А. Петрович, 
  А. Ливер, И.В. Яблочкина, И. Пыхалов<sup>26</sup>, не 45 тысяч, как считает 
  П. Полян<sup>27</sup>. <br>
  <br>
  Из 70 станиц терских казаков с населением 277 891 человек Советская власть предполагала, 
  как уже говорилось, выселить 18 станиц — 60 тысяч человек — из сунженского отдела 
  (в нем было 20 станиц), поскольку именно в этом отделе казачьи земли клином 
  вдавались в горские. <br>
  <br>
  К 14 апреля 1921 г., когда Президиум ВЦИК запретил переселение, население 11 
  станиц покинули свои дома: 8 станиц были переселены, 3 станицы разграблены, 
  население их покинуло, выселять их не пришлось, но их тоже причислили к выселенным. 
  <br>
  <br>
  По подсчетам членов комиссии Казачьего отдела ВЦИК Шабунина и Гришина (документ 
  №32), в 1918 г. (и вторично, весной 1920 г.) был выселен из трех станиц 8671 
  человек, в ноябре-октябре 1920 г. из пяти станиц — 21 806 человек. С учетом 
  трех разоренных нападениями на них станиц, население которых покинуло их без 
  выселения, авторы доклада определяют число оставивших свои дома казаков в 34 
  637 человек. <br>
  <br>
  Следует заметить, что подсчет населения станиц производился «с исчислением хозяйств 
  и населения по переписи 1916 г.». Конечно же, к концу 1920 г. население казаков 
  в станицах убавилось, поскольку убыль всего населения Терской области по переписи 
  1920 г. составила по сравнению с 1917 г. 16% всего населения. В Терской области 
  в 1917 г. проживало 1 021 918 человек, в 1920 г. — 905 001.<sup>28</sup><br>
  <br>
  С.М. Киров назвал число выселенных терских казаков — 22 тысячи (документ №23). 
  Председатель комиссии ВЦИК по наделению горцев землей в феврале 1921 г. доложил 
  во ВЦИК, что из Терской области «выселено более 25 тысяч трудового населения» 
  (документ №29. Эта же цифра получается из сопоставления других источников. А.И. 
  Деникин говорил о «примерно 10 тыс[ячах] человек», выселенных из 3-х станиц 
  в мае 1920 г.<sup>29</sup> А в докладе председателя комиссии по установлению 
  границ Горской АССР Муромцева во ВЦИК от 28 сентября 1921 г. в котором речь 
  идет о восставших станицах, выселенных в ноябре-декабре 1920 г., сказано, что 
  он «расселил до 15 тыс[яч] казаков Сунженской линии» (документ №40). Так что 
  цифра выселенных из Терской области казаков «примерно 25 тыс[яч] человек» наиболее 
  близка к истине. Она составляет примерно 1/10 часть терского казачества. <br>
  <br>
  Некоторые исследователи расходятся в определении мест, куда переселяли терских 
  казаков, А. Ливен считает, что чеченцы, проживавшие на равнине, не допускали 
  передачу казачьих станиц горской чеченской бедноте. К расходам по переселению 
  казаков прибавилась утрата имущества в покинутых станицах, которое было разграблено, 
  а продовольствие и фураж гнили «в тысячах пудов», т.к. их было нельзя вывести 
  «за непредставлением средств передвижения по железной дороге». Большевики намеревались 
  «установить, наконец, мир между горцами и казаками». Не получилось и это. Напротив, 
  резко обострились отношения между ними, резко усилился местный национализм — 
  горцы теперь требовали не только уничтожения чересполосицы, но и выселения из 
  ГАССР всех казаков. Советская власть не побоялась обидеть своей политикой всех 
  терских казаков, даже тех, кто поддерживал ее. Она сама способствовала уходу 
  казаков в повстанческие отряды. (документ № 19, 21, 22, 25-38, 41).<br>
