  пустующие места (например, станица Михайловская) доброкачественных земель, где 
  возможно уравнительное землепользование. Необходимо присутствие русского населения 
  вокруг столь важного горнозаводского района Грозного, требующего усиленного 
  снабжения продовольственными продуктами; горское население, слабое в сельскохозяйственной 
  производительности, удовлетворить рабочих продуктами не может, что наглядно 
  засвидетельствовали в настоящее время государственной разверсткой, которая все 
  [в] наибольшей мере, почти исключительно, выполнялась казачье-крестьянским населением. 
