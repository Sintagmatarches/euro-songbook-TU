  В декабре 1920 г. во ВЦИК Советов Терская казачье-крестьянская делегация представила 
  доклад, по которому Президиум ВЦИК Советов 27 января 1921 г. постановил: немедленно 
  приостановить переселение казаков из пределов ГАССР и образовать на месте комиссию 
  по равному представительству горцев и казаков под председательством уполномоченного 
  ВЦИК Невского. Комиссии вменялось в обязанность изучить на месте вопрос о наделении 
  безземельных и малоземельных казаков. Таковая комиссия образована во Владикавказе, 
  и 1 марта начала свою работу. <br>
  <br>
  В основу работ комиссии были взяты все статистические сведения о числе народонаселения 
  и межевые данные о количестве земель в ГАССР из Терского Областного Статбюро, 
  причем сведения о наличии земель в ГАССР не оспаривались никакой стороной комиссии, 
  сведения же о числе народонаселения казались неверными. <br>
  <br>
  Например, по переписи 1920 г. Облстатбюро чеченцев насчитывается 273,358 душ, 
  по заявлению представителя Чечни Мальсогова, чеченцев, якобы, имеется 400 тысяч; 
  по заявлению другого представителя Чечни Гайсулова, указывалось, что их 500 
  тысяч, каковые цифры не были подтверждены. <br>
  <br>
  Подобные заявления были об осетинах и ингушах. <br>
  <br>
  Областное Статбюро все дефекты по переписи 1920 г. (таких было мало) дополнило 
  сведениями 1916 и 1917 гг. с процентными прибавками прироста рождаемости. <br>
  <br>
  По данным Областных Статбюро, количество всей земли в ГАССР исчисляется в 2,854,796 
  десятин, из них удобной — 2,376,963, неудобной— 477,833 десятины. <br>
  <br>
  Из числа удобной — леса с кустарником — 412,548 десятин, удобной без леса — 
  1,964,415 десятин, из коего числа сенокоса — 281,402 десятин, выгона — 809,791 
  десятин, под усадьбами — 17,214 десятин, пашни — 856,008 десятин. <br>
  <br>
  Население исчисляется по переписи 1920 г. и частью 1916 и 1917 гг. в 703,312 
  душ, число хозяйств 124,662. <br>
  <br>
  Если провести уравнительное землепользование, то всей земли на одно хозяйство 
  приходится 22,9 десятины, одной удобной — 19 десятин, удобной без леса - 15,7 
  десятин, пашни — 6,87 десятин. На душу всей земли приходится - 4 десятины, одной 
  удобной — 3,3 десятин, удобной без леса - 2,79 десятин, пашни — 1,2 десятин. 
  Все эти земли лучшего качества и исчисленное количество должно вполне удовлетворить 
  все земледельческое население ГАССР. <br>
  <br>
  Члены Комиссии, представители горского населения этим не удовлетворилось и высказывались 
  на заседании 7 марта 1921г. за абсолютное выселение всех станиц за пределы ГАССР, 
  основываясь на исторических данных, что эти земли когда-то принадлежали их предкам 
  и теперь должны принадлежать им. <br>
  <br>
