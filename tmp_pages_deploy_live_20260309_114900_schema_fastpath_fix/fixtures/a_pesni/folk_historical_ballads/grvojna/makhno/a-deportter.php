  выехал в Пятигорск, откуда вместе с Орджоникидзе (прибыл из Ростова) и Кировым 
  продолжил объезд Северного Кавказа, принимая непосредственное участие в организации 
  областных, окружных и национальных ревкомов, создавая инструкции, разрабатывая 
  штаты и сметы, издавая приказы. <br>
  <br>
  Разоренные и сожженные аулы и станицы, ограбленные белыми бандами, ингуши, чеченцы, 
  осетины — горцы, - вот что встречало нас на нашем пути. И эти дикие народы встречали 
  нас еще с большей радостью, чем ставропольские крестьяне. <br>
  <br>
  В Грозном мы попали на чеченский съезд и утвердили на нем чеченский ревком, 
  облеченный всей полнотой власти. Составленная мной и принятая единогласно резолюция 
  съезда приветствовала восстановление Советской власти в Чечне и отмечала необходимость 
  существования единой рабоче-крестьянской российской централизованной власти. 
  Когда мы приехали в Назрань, эту, так сказать, столицу Ингушетии, весь ингушский 
  народ, начиная от 16-летних мальчиков до седобородых стариков с красными и зелеными 
  знаменами во главе со своими муллами верхом и в полном вооружении устроили нам 
  торжественную встречу. <br>
  <br>
  В связи с предоставлением ингушам земель 3-х казачьих станиц (выселенных нами 
  неорганизованно), настроение горцев всецело было на стороне Советской власти. 
  <br>
  <br>
  Ингуши и чеченцы издавна были ограблены царским правительством в пользу казаков, 
  которые были населены на насильственно отторгнутых у горцев землях, создавая 
  тем самым искусственную чересполосицу. Естественно, что Советская власть должна 
  была восстановить законные права горцев на некогда принадлежавшую им землю, 
  но делать это она должна была исключительно организованным путем. Вот почему 
  Северо-Кавказским ревкомом была создана специальная переселенческая комиссия, 
  которая должна была разработать пути безболезненного переселения подлежащих 
  выселению жителей станиц на другие земли. Но работа ее до сих пор еще не налажена. 
  Это подрывает авторитет Советской власти. <br>
