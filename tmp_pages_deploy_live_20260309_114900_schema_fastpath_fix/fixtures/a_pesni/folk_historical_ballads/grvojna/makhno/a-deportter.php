  восстания. Повстанцы, занявшие станицу Калиновская и часть железной дороги от 
  нее, были разбиты и отошли в северном направление на Буруны. <br>
  <br>
  Через некоторое время после восстания станиц Надтеречной линии 18 октября вспыхнуло 
  восстание в станицах Сунженской линии, оно имело связь со станицами Надтеречной 
  линии. Участниками его являлись казаки станиц: Ермоловская, Закан-Юрт (Poмановская), 
  Самашкинская и Михайловская. Как и станицы Надтеречной линии, станицы Сунженской 
  линии вначале имели успех. Часть железнодорожных линий на участке Грозный — 
  Беслан, начиная от станиц Ермоловской до Михайловской включительно, находилась 
  в руках восставших. <br>
  <br>
  Высланные первоначально части из-за многочисленности [повстанцев] успеха не 
  имели и повстанцы подошли почти вплотную к Грозному. Положение создавалось серьезное: 
  Все боевые части и часть рабочей армии находились на Надтеречной линии и усилить 
  части на Сунженской линии было нечем. Поэтому пришлось призвать под ружье коммунистов 
  всех организаций, снять все инженерные части с работы и затребовать помощь с 
  фронта. <br>
  <br>
  Благодаря срочно принятым мерам, восстание удалось в 3-4 дня ликвидировать и 
  восстановить прерванное железнодорожное сообщение с Владикавказом и Прохладной. 
  <br>
  <br>
  Чтобы раз и навсегда покончить с контрреволю цией на Кавказе, член РВС Кавказского 
  фронта товарищ Орджоникидзе, проезжая чрез Грозный, приказал: станицу Калиновскую 
  (Надтеречная) сжечь, а станицу Ермоловская, Закан-Юрт (Романовская), Самашкинскую 
  и Михайловскую (Сунженская линия) выселить и передать беднейшему горскому народу 
