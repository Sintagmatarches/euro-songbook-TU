  поселениями ко вновь образованной Терской губернии. <br>
  <br>
  Это было бы справедливым, т.е. в интересах рабочего класса всей РСФСР. <br>
  <br>
  С товарищеским приветом К. Еремеев. 19/VII-21 г. Москва. <br>
  <br>
  <em>ГАРФ. Ф. 1318. Оп. 1. Д. 211. Л. 443. </em><br>
  <br>
  <br>
  <strong>Из доклада в РКИ представителя горцев Геланова. Июль 1921 г. </strong><br>
  <br>
  Вновь заселенные станицы Михайловская, Самашки, Закан-Юрт, Ермоловская начали 
  заселяться 4 месяца назад. <br>
  <br>
  В станицы возвращались казаки, устроились на железную дорогу примерно 150 семей, 
  в местную больницу более 50 семей. Когда образовался в станице Михайловской 
  исполком, он начал хлопотать через Чечокрисполком об их выселении. Дошло до 
  ГорЦИКа, он приказом от мая 1921 г. выселил их. Они тогда обращались с ложными 
  жалобами к красногвардейцам, возбуждали ненависть к чеченцам. Чеченцы хотят 
  даже покидать станицы, уйти опять в горы. С прибытием в станицу Михайловскую 
  красноармейцев на работу (40 бригада 14-й дивизии и резервная сотня) положение 
  переселенцев ухудшилось: красноармейцы отбирали у них косилки, грабили, последний 
  фураж по амбарам, чуреки, молоко требуют, угрозы выселить и т.д. <br>
  <br>
  Казаки грозятся сжечь озимую пшеницу, которую им не дали убрать. Казаки тайком 
  косят пшеницу, увозят ее, когда за ними гоняться, - они стреляют. Красноармейцы 
  содействуют в этом казакам, которые привозят красноармейцам продукты: мясо, 
  хлеб... <br>
  <br>
  Подводной повинностью переселенцы обложены непосильно. Ежедневно у них требуют 
  подводы Армия, агенты разных учреждений. <br>
  <br>
  Покосов переселенцы лишились. Что было ими скошено, увезено красноармейцами 
  в Грозный, отбирают грабли, косилки, косы, точила. <br>
  <br>
  Курортная милиция, железнодорожная милиция и человек 7 местной милиции состоят 
  из казаков станиц Михайловская и Слепцовская. Ведут себя по отношению к переселенцам 
  возмутительно: обстреливают идущих на работу, арестовывают, отбирают оружие. 
  Курортная милиция находится в подчинении начальника милиции Сунженского округа 
  в станице Слепцовской и потому действует, как ей заблагорассудится, и приносит 
  только вред переселенцам. <br>
  <br>
  Окружная милиция Сунженского округа расположена в станице Слепцовской. При провозе 
  закупаемой пшеницы в Назрановском округе переселенцы задерживаются в Слепцовской, 
  у них отбирается и кукуруза, и оружие. На запросы Михайловского исполкома о 
  чинимых обидах и беззакониях начальник милиции не отвечает. <br>
  <br>
  Раздел хлебов переселенцам происходил тоже незаконно. Ждали черезвычайную тройку 
  от 3 июля. Когда же приехал уполномоченный, числа 8-9 июля, сказал, что срок 
  уборки до 11 июля, и так как хлеб не убран, пришлют Кавтрудармию. Трудармия 
  прибыла, им отвели 1,000 десятин. Так что переселенцам косить нечего. <br>
  <br>
  Сады (около 10 лучших) отвели работникам курорта Серноводской железной дороги 
  и рабочим больницы — есть распоряжение Земотдела Чеченского округа. Рабочие 
  под охраной милиции или красноармейцев с бронепоезда едут в сады и забирают 
  зеленую фрукту, ломают вишни, айву, персики, курагу, уничтожают беспощадно, 
  без сожаления. <br>
  <br>
  Необходимо выселить казаков, убрать красноармейцев, особенно бронепоезд, т.к. 
  переселенцы ведут себя спокойно. <br>
  <br>
  Порочные лица станицы Михайловской совершают кражи окон, дверей, рам, строительного 
  материала. При распределении домов были несправедливости: председатель Подкомиссии 
  Мачко Улаев взял себе и родственникам лучшие дома, не по жребию. <br>
  <br>
  В станице Самашки дома распределялись за взятки. Тоже происходит разорение домов, 
  кражи. <br>
  <br>
  В станице Закан-Юрт милиция занимается кражей, скрывает уворованное. У милиционера 
  нашли уворованного бычка и лошадей. <br>
  <br>
  Выводы. <br>
  <br>
  Местные исполкомы неспособны вести борьбу с порочными лицами, провокаторами. 
  В каждой станице необходимо избрать милицию, 10-15 человек, с поручительством 
  обществ об их благонадежности. Возложить на них охрану порядка. Подчинить милицию 
  местным исполкомам. <br>
  <br>
  В станице Михайловской, как прилежащей к Нестеровской, Слепцовской, Ассиновской, 
  установить усиленную самооборону, особенно ночью. Снабдить их патронами штук 
  до 10 тысяч на случай бандитов или восставших казаков. <br>
  <br>
  Курортную милицию заменить, удалить Михайловскую милицию (в ней 8 человек Михайловских 
  казаков выселенных по I категории). Казаков уволить и выслать из станицы. <br>
  <br>
  Предст. Геланов. <br>
  <br>
  <em>Бывший партархив ЧИАССР. Ф. 233. Оп. 1. Д. 1. Л. 25. </em><br>
  <br>
  <br>
  <strong>Из постановления IV съезда Советов Осетинского округа. 23.07.1921 г. 
  </strong><br>
  <br>
  1) Вновь настаивать на выселении казаков... <br>
  <br>
  3) Выразить категорический протест против действий и работы Невского, не побывавшего 
  ни в одном горском ауле и пристрастно и преступно осветившего вопрос о земельном 
  голоде горцев на заседании Президиума ВЦИК от 14 апреля 1921 г. в пользу казачества. 
  <br>
  <br>
  4) Протестовать против постановления ВЦИК от 14 апреля 1921 г. за №21, являющегося 
  результатом неправильного освещения вопроса Невским и ничего не дающего в результате 
  для горцев. <br>
  <br>
  5) Просить ГЦИК в срочном порядке довести протест до сведения ВЦИК и настаивать 
  на отмене постановления №21. <br>
  <br>
  <em>ГАРФ. Ф. 1318. Оп. 6. Д. 2. Л. 15. </em><br>
  <br>
  <br>
  <strong>Из протоколов заседания Пленума Кавказского бюро ЦК РКП(б). 15.08.1921 
  г. </strong><br>
  <br>
  Предложить Чеченскому исполкому в 3-х месячный срок заселить станицы Сунженской 
  линии, предоставленные для чеченской бедноты. В случае не- заселения станица 
  Ермоловская переходит в распоряжение Грозненского исполкома для организации 
  рабочего поселка. Ответственность за исполнение этого постановления ложится 
  на председателя ГорЦИКа Эльдарханова. <br>
  <br>
  <em>РГАСПИ. Ф. 64. Оп. 2. Д. 1. Л. 132. </em><br>
  <br>
  <br>
  <strong>Из тезисов «Год деятельности кавказской Армии Труда (11 сентября 1920 
  г. - 11 сентября 1921 г.)». </strong><br>
  <br>
  Период с 11 сентября 1920 г. по 1 января 1921 г. был периодом тяжелых испытаний 
  для Кавтрудовой. Окруженная со всех сторон врагами, отрезанная от Ростова, Владикавказа, 
  не имея значительных сил, она спасла нефтяной район, железнодорожные узлы, не 
  дав соединиться бандам Надтеречного района с бандами Сунженской линии, разбив 
  их поодиночке, т.е. не дала движению развиться к югу и востоку в горных районах 
  области, дала возможность без перерыва эксплуатировать богатства края, доставлять 
  нефть. <br>
  <br>
  С 1 января 1921 г. ликвидировать противника и, передав боевые участки войскам 
  Теробласти, начала переходить на трудовые положения снова. <br>
  <br>
  <em>РГАСПИ. Ф. 17. Оп. 13. Д. 1104. Л. 57; Бывший партархив ЧИ АССР. Ф. 249. 
  Оп. 1. Д. 4. Л. 109. </em><br>
  <br>
  <br>
  <strong>Из доклада в Президиум ВЦИК председателя смешанной комиссии ВЦИК по 
  установлению границ ГАССР Муромцева. 28.09.1921 г. </strong><br>
  <br>
  С 26/VII, когда я получил официальное подтверждение от члена Президиума ВЦИК 
  и от Наркомнаца товарища Сталина, что я назначаюсь представителем ВЦИК и председателем 
  смешанной комиссии по установлению границ Горской республики, я приступил к 
  работам. <br>
  <br>
  До этого времени я как представитель Крайэконмсовета Юго-востока России сделал 
  подготовительные работы и расселил до 15 тысяч казаков Сунженской линии, выселенных 
  из станиц Михайловской, Самашкинской, Закан-Юртовской, Ермоловской и др. по 
  Ставропольской, Терской губерниям, Кубанской и Донской областям. <br>
  <br>
  <em>ГАРФ. Ф. 1318. Оп. 1. Д. 51. Л. 170. </em><br>
  <br>
  <br>
  <strong>Из протокола заседания Президиума Федерального комитета по земельным 
  делам. 06.10.1921 г. <br>
  </strong><br>
  Слушали: доклад о положении земельного дела в ГАССР. Доклад делал нарком земледелия 
  ГАССР товарищ Датиев (доклад основан на трудах Абрамовой комиссии, образованной 
  в 1906 г. и на других материалах). Содержание доклада в книге «Земельный вопрос 
  в ГАССР». <br>
  <br>
  Постановили: 1) Признать, что население нагорной полосы ГАССР испытывает чрезвычайное 
  малоземелье, граничащее с земельным голодом. 2) Удовлетворить их землей на плоскости. 
  3) Так как в ГАССР свободных земель нет, часть населения из ГАССР должна быть 
  выселена. 4) Послать для выяснения этого вопроса 1 ноября 1921 г. комиссию. 
  <br>
  <br>
  <em>ГАРФ. Ф. 1235. Оп. 1. Д. 149. Л. 103. </em><br>
  <br>
  <br>
  <strong>Из сведений Северо-Кавказского краевого Земельного управления. 03.11.1921 
  г. </strong><br>
  <br>
  Горские народности, образующие ныне автономные области (Адыго-Черкесская, Карачаевская, 
  Кабардино-Балкарская, Осетинская, Ингушская, Чеченская, Черкесский округ) до 
  революции входили в состав населения бывшей Области Войска Терского, за исключением 
  Адыгее-черкесов, входивших в состав населения бывшей Области Войска Кубанского. 
  <br>
  <br>
  Статистические данные по вопросам землепользования в прошлом национальных округов 
  бедны, с одной стороны, потому, что они мало исследованы вообще, а, с другой 
  стороны, имеющиеся данные не являются результатом серьезного, продуманного труда. 
  По данным бывшей Горской республики только в 1920 г. было размещено 10,5 тысяч 
  горцев и 2 тысячи плоскостных ингушей в выселенных 4 станицах, включенных в 
  Ингушетию... Расширение площади трудопользования произошло (к 1926 г. — Е.Ж.) 
  в Чечне, главным образом, за счет переданных Чечне освобожденных от казаков 
  5 станиц и 2 русских слобод. <br>
