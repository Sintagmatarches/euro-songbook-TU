  армию для работ. Семьи казаков с самой необходимой домашней утварью в Моздокский 
  и Кизлярские районы, но не ближе 50 верст от данной станицы. Семьи советских 
  служащих и лиц, сочувствующих советской власти, направлены в более лучшие районы. 
  <br>
  <br>
  Главным тормозом при выселении явилось отсутствие транспорта, что особенно стало 
  заметно, когда началось выселение семейств. Для скорейшего разрешения и этого 
  сложного вопроса станицу Калиновскую, предназначенную вначале для сожжения, 
  было разрешено использовать для населения семьями из станиц Сунженской линии. 
  Эта мера значительно облегчила работу по выселению. Пока закончено полностью 
  выселение станицы Ермоловской. <br>
  <br>
