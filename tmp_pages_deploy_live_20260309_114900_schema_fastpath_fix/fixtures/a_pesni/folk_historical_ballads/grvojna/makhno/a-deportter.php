  кавказского фронта Орджоникидзе из представителей от Терского облисполкома, 
  чеченцев, под председательством Председателя Чеченского окружного исполкома 
  товарища Эльдарханова. <br>
  <br>
  Проведение отмеченной репрессии произвело на казаков должное впечатление. Они 
  притихли, почувствовали силу Советской власти, стало заметно успокоение. Разбитые 
  отряды рассыпались и лишь изредка делают налеты на станицы и хутора, которые 
  сводятся, в основном, к грабежу... <br>
  <br>
