  <br>
  Ставропольское крестьянство с радостью узнавало о разгроме деникинских банд 
  и восторженно приветствовало нас как представителей центральной власти. Оставив 
  в Ставрополе значительное количество привезенных Кавбюро (Кавревком) из Астрахани 
