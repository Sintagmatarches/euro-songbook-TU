  <br>
  Ай, да по крутому, по красному, <br>
  По красному бережку, <br>
  Ай, да по желтому по песку, <br>
  По песку сыпучему, <br>
  <br>
  Ай, вот ходили там раздобрые, <br>
  Вот добрые молодцы, <br>
  Ай, да гуляли там вольные, <br>
  Вольные казачушки, <br>
  <br>
  Ай, что донские, они гребенские, <br>
  Казаки я<strong>и</strong>цкие. <br>
  Ай, они думали вот думушку, <br>
  Вот думушку крепкую. <br>
