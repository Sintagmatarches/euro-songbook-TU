  Летом 1630 г. из Москвы были отправлены в Стамбул послы, везшие также грамоту 
  донским казакам, где им предписывалось совместно с турками выступить против 
  польского короля. Грамота вызвала недовольство казаков. Сопровождавший послов 
  воевода Иван Константинович Карамышев был изранен и утоплен в Дону на основании 
  слуха, что якобы он имел намерение принудить казаков к подчинению туркам. Позже 
  казаки пытались представить причиной убийства непочтительность Карамышева.<br>
