  <br>
АЙ, НУ, КАК ПО МОРЮ, ПО МОРЮ </h1>
  <br>
  Ай, ну, как п<strong>о</strong> морю, по м<strong>о</strong>рю, <br>
  <div class='refren'>Морю синему, <br>
 По тому было по морю, по Верейскому, <br>
Там плывут же, восплывают всё на триста носов,</div>
  Ай, там плывут же, восплывают <br>
  <div class='refren'>Всё на триста носов, <br>
  Как на трис[ы]та носов, да на пятьсот кораблёв.<sup>1</sup><br>
  Как один-то с них кораблик поперёд резко бежит, </div>
  Ай, как один-то с них кораблик <br>
  <div class='refren'>Поперёд резко бежит, <br>
  Поперёд он возбегает, что ясмён сокол летит, <br>
  Как и нос-то со кормою — по-звериному, </div>
  Ай, как и нос-то со кормою – <br>
 <div class='refren'>По-звериному, <br>
 А бока-то сведённые по-змеиному, <br>
 Ну, как голову он держит по-кониному, </div>
  Ай, ну, как голову он держит <br>
  <div class='refren'>По-кониному, <br>
Как на место-то ушей в него навешено, <br>
  Что по чёрному по соболю сибирскому, </div>
  Ай, что по чёрному по соболю <br>
 <div class='refren'>Сибирскому, <br>
Как на место-то бровей в судёницы врезано, <br>
  Что по чёрной по кунице по пещеровской; </div>
  Ай, что по чёрной по кунице <br>
 <div class='refren'>По пещеровской; <br>
  Как на место очей-то в судне вклеено, <br>
 Что по жёлтому по камушку по яхонту, </div>
  Ай, что по желтому по камушку <br>
   <div class='refren'>По яхонту, <br>
 Хорошо было судёнце изукрашенное, <br>
 Оно разными красками измалёванное, </div>
  Ай, оно разными красками <br>
  <div class='refren'>Измалёванное. <br>
  На судне была беседушка построенная, <br>
 Ну, построена беседа кипарисовая, </div>
  Ай, ну, построена беседа <br>
  <div class='refren'>Кипарисовая. <br>
  Во беседе стоят столики дубовые, <br>
  На них скатерти разостланы шелковые, </div>
  Ай, на них скатерти разостланы <br>
  <div class='refren'>Шелковые, <br>
 Ну того ли шелку шемахинского.<sup>2</sup> <br>
  На столах стояли пойла, пойла пьяные, </div>
  Ай, на столах стояли пойла, <br>
 <div class='refren'>Пойла пьяные, <br>
Пойла пьяные стояли, мёды сладкие, <br>
 Ну закусочка лежала всё сах<strong>а</strong>рная, </div>
  Ай, ну закусочка лежала <br>
   <div class='refren'>Всё сах<strong>а</strong>рная, <br>
Ну сах<strong>а</strong>рная закуска, гуси-лебеди. <br>
  За столами-то сидели, на двенадцать молодцов,</div>
  Ай, за столами-то сидели, <br>
   <div class='refren'>На двенадцать молодцов, <br>
  На двенадцать молодцов-то, всё донских казаков. <br>
  У них волос-то, был в волос, как один-то колос, </div>
  Ай, у них волос-то был в волос, <br>
  <div class='refren'>Как один-то колос, <br>
 Ну лицо было в лицо-то, как единое лицо. <br>
  На них платьице пошито, одного было сукна, </div>
  Ай, на них платьице пошито, <br>
  <div class='refren'>Одного было сукна, <br>
  Одного сукна пошито, одного мастера. <br>
 Как один-то был молодчик, он почище всех, </div>
  Ай, как один-то был молодчик, <br>
 <div class='refren'>Он почище всех, <br>
Он почище, да покраше, да повежливей. <br>
  По судёницу молодчик всё похаживает, </div>
  Ай, по судёницу молодчик <br>
   <div class='refren'>Всё похаживает, <br>
 Во звончатые он во гусельки поигрывает, — <br>
  Вспоминает он батюшку славный Тихий Дон. </div>
  <br>
  <sup>1</sup> Вар.: Как на триста носов, да пять легких кораблёв. <br>
  <sup>2</sup> Вар.: Самашинского, Рамашинского. <br>
