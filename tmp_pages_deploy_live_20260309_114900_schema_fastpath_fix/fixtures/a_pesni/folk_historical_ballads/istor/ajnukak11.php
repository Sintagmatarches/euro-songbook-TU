  <div class='refren'>Морю по Верейскому,<sup>1</sup> <br>
   По Дону по Ти... да по Тихому<sup>2</sup><br>
   Ой, да что плывут-то, плывут, <br>
   Они восплывают, <br>
   Вот бы черноморские, <br>
  Плывут три стружо... ой, три стружочка. </div>
   А-ой да, что плывут-то, плывут, они восплывают, <br>
  <div class='refren'>Вот бы, черноморские, <br>
  Плывут три стружо... три стружочка; <br>
  Ой да, как один-то из них <br>
  Лёгенький стружочек, <br>
  Вот и был он резвый, — <br>
  Вперёд возбега ... ой, возбегает. </div>
  [А]-ой да, как один-то из них лёгенький стружочек, <br>
  <div class='refren'>Вот и был он резвый, — <br>
  Вперёд возбега ... ой, возбегает. <br>
  Ай да, как на этом-то <br>
  Лёгеньком стружочке <br>
  Там людей немножко, — <br>
  Семеро гулё... семь гулёбщичков.</div>
  [А-ой] да, как на этом-то лёгеньком стружочке <br>
  <div class='refren'>Там людей немножечко, — <br>
  Семеро гулё ... семь гулёбщичков, <br>
  Семеро гулёбщичков, <br>
  Молодых казаченьков, <br>
  Вот и с Дону Тихого, <br>
  Удалых охо... всё охотников. </div>
  [Ай] семеро гулёбщиков, молодых казаченьков, <br>
  <div class='refren'>Вот и с Дону Тихого <br>
  Удалых охо... всё охотничков. <br>
  Ой да, ну, восьмой-то у них, <br>
  Восьмой воду носит, <br>
  Вот и, а девятый-то <br>
  Сидит ружьё чи... ружьё чистит. </div>
  [А]-ой да, ну, восьмой-то у них, восьмой воду носит, <br>
 <div class='refren'>Вот и, а девятый-то <br>
  Сидит ружьё чи... ружьё чистит. <br>
  Ой, да, как десятый молодец <br>
  Вот был сам хозяин, <br>
  Он сам атаман — <br>
  Степан Тимофе... Тимофеевич. </div>
  [А]-ой, да, как десятый молодец вот был сам хозяин, <br>
  <div class='refren'>Он сам атаман — <br>
  Степан Тимофе... Тимофеевич.<br>
  Ой да, по кораблику <br>
  Он ходит, гуляет, <br>
  Песенки всё вольные, <br>
  Песни распева... распевает.<sup>3</sup></div>
  [А]-ой да, по кораблику он ходит, гуляет, <br>
 <div class='refren'>Песенки всё вольные, <br>
  Песни распева ... распевает, <br>
  Ой да, сам во гусельки, <br>
  Он во гусли звончатные<sup>4 </sup><br>
  Заливно<sup>5</sup> играет, — <br>
  Друзей забавля... забавляет. </div>
  [А]-ой да, сам во гусельки он во гусли звончатные <br>
  <div class='refren'>Заливно играет, — <br>
  Друзей забавля... забавляет. <br>
  Ой да, вы, работнички, <br>
  Разински охотнички! <br>
  Вы ударьте легкими, <br>
  Бабайками до... братцы, долгими. </div>
