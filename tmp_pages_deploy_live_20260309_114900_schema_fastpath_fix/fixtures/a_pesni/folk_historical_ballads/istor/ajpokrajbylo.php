  <br>
  Турецкая Азовская крепость в устье Дона перекрывала выход донским казакам в 
  Азовское и Черное моря и служила оплотом для набегов на окрестные земли. Летом 
  1637 г. донские казаки при поддержке запорожцев осадили и 18 июня (ст.ст.) взяли 
  штурмом Азов. Они предложили город русскому царю в обмен на военную помощь, но царь не принял предложения. Казаки удерживали Азов до 1643 г. и до этого времени город был столицей Донского войска.<br>

