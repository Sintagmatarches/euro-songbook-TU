<strong>АЙ, ПО КРАЙ БЫЛО МОЯ СИНЕГО</h1>
  <br>
  Ай, по край было моря синего,<br>
  Что на устье Дону тихого,<br>
  На крутом красном бережку,<br>
  На желтых рассыпных песках.<br>
  А стоит крепкий Азов-город<br>
  Со стеною белокаменною,<br>
  Земляными раскатами и ровами глубокими,<br>
  И со башнями караульными...<br>
