  ЛЮБО, БРАТЦЫ, ЛЮБО</strong><br></span>
  <br>
  Как на черный берег выгнали татары,<br>
  Супротив казаков сорок тысяч лошадей,<br>
  И покрылось поле, и покрылся берег,<br>
  Сотнями порубанных, пострелянных людей.<br>
  <br>
  <em>Припев (дважды):</em><br>
  <br>
  Любо, братцы, любо, любо, братцы, жить,<br>
  С нашим атаманом не приходится тужить.<br>
  <br>
  А первая пуля, а первая пуля,<br>
  А первая пуля, пуля ранила меня,<br>
  А вторая пуля, а вторая пуля,<br>
  А вторая пуля - моего коня.<br>
  <br>
  А жена разлюбит, выйдет за другого,<br>
  Выйдет за другого и забудет про меня,<br>
  Жалко только матушку, матушку-старушку,<br>
  Матушку-старушку, да буланого коня.<br>
  <br>
  Вот умру в степи я, над моей могилой,<br>
  Разнесет лишь ветер только сорную траву,<br>
  Где сложил под саблями, под саблями татарскими,<br>
  Буйну, да кудряву, да красиву голову.<br>
  <br>
  <em>С фольклорного сайта <a href="http://ingeb.org/songs/ljubobra.html" target="_blank">ingeb.org</a></em><br>
 <br>
  <br>
  Происхождение песни связывают с турецкими войнами или Кавказской войной, однако достоверно она фиксируется с XX века. Сложена  ровным литературным поэтическим размером (что исключает &quot;народное казачье&quot; происхождение), хотя размер похож на &quot;старый&quot; - близкий размер использовал в середине XVIII в. А.П. Сумароков (см., например, песню <a href="../18/teplov13.php">"Мы друг друга любим"</a>, Х6ц5жм, а из песен XX в. тем же размером сложена <a href="../dvor/murka.php">"Мурка"</a>). Текст припева перекливается с первым куплетом <a href="../army/pposleutch.php">"Песни после ученья"</a> армейского поэта Н. Веревкина, сочиненной в 1820-30-е гг. (размер тоже близкий - Х3жм): <br>
  <br>
  <em>Молодцам-солдатам <br>
Не о чем тужить! <br>
С командиром-хватом <br>
Любо, братцы, жить!</em><br><br> 
Дополнительный довод в пользу литературности - отстутствие строгого сюжета. Это романтическая баллада, а не историческая песня, и версия о том, что в основе - реальное событие, сомнительна.<br>
  <br>
  По мнению барда и искусствоведа Бориса Алмазова, это песня о конкретном сражении между ордой ногайских и крымских татар хана Девлет-Гирея и 
  донскими полками Платова и Ларионова на реке Калалах (совр. написание - &quot;Калалы&quot;) 
  3 апреля 1774 г. Разные источники называют число татар 20 или 16 тысяч всадников 
  - отсюда якбы упоминание &quot;40 тысяч лошадей&quot;, если считать по две заводные лошади на татарского всадника. <br>
  <br>
  Полки донских казаков Платова и Ларионова шли в авангарде обоза полковника Бухвостова, 
  который вез на Кубань припасы для гарнизонов пограничной линии. Узнав о приближении 
  Девлет-Гирея, донцы из повозок соорудили лагерь и окопались. Несколько атак 
  они отбили, но силы были явно неравными. Всем казакам пришлось бы погибнуть, 
  но на выручку подоспел и атаковал лавой казачий полк Уварова. Казаки Платова 
  и Ларионова сели на коней и ударили со своей стороны. Татары стали отступать 
  и налетели на гусарский полк Бухвостова, который их тоже атаковал. Крымцы и ногайцы понесли большие потери.  С этого боя началась слава Матвея Платова - тогда еще 23-летнего 
  полковника, а в будущем донского атамана и героя многих войн, вплоть до войны 
  1812 года. <br>
  <br>
  <br>
  <strong>Из книги Бориса Алмазова &quot;Не только музыка к словам&quot;, М., 
  &quot;Центрполиграф&quot;, 2003, с. 354: </strong><br>
  <br>
  <em>&quot;На Великой Грязи, там где Чёрный ерик,<br>
  Татарва нагнала сорок тысяч лошадей,<br>
  Замутился ерик, и покрылся берег<br>
  Сотнями порубанных, пострелянных людей...<br>
  <br>
  Оборона донским полковником М.И. Платовым каравана при урочище Каллах, на Черногрязском 
  шляхе, от 16-тысячной орды нагайцев и крымцев. Удивительно, даже число лошадей, 
  татары вели по две &quot;заводные лошади&quot;, примерно совпадает&quot;.<br>
  </em> <br>
  <br>
  <em>Ерик – </em>старица, непроточный рукав покинутого русла реки.<em></em><br>
  <br>
  <br>
  <strong>Из книги генерала Петра Краснова &quot;Картины былого Тихого Дона&quot; 
  (1909):</strong><br>
  <br>
  <em>&quot;В 1773 году крымский хан Девлет-Гирей, чуя погибель Крыма, покоряемого 
  русскими войсками Долгорукого, возмутил кубанских татар, и они стали собирать 
  большую рать.<br>
  <br>
  В это время на Кубань шел обоз. Везли казакам на линию провиант и припасы, ехали 
  переселенцы на новые места, гнали скот, верблюдов. Этот огромный обоз вел полковник 
  Бухвостов с двумя полками казаков — Матвея Платова и Ларионова, и двумя пушками.<br>
  <br>
  В авангарде шли Платов и Ларионов. Была ранняя весна, степь зацвела. 3 апреля 
  полк Платова расположился на ночлег в глухой степи у р. Калалах, недалеко от 
  Ейска. Стих гомон казачьих голосов, лошади поели корм и дремали, переминаясь 
  с ноги на ногу. Платов, молодой 23-летний полковник, только что устроился спать, 
  как к нему в палатку заглянул старый, не раз бывавший в закубанской степи казак.<br>
  <br>
  — Матвей Иванович, — тихо сказал он, — подь сюда на минутку.<br>
  <br>
  Платов быстро оделся и вышел с казаком в открытую степь.<br>
  <br>
  — А ну, приляг ухом к земле, — сказал Платову казак. Платов прилег.<br>
  <br>
  — Ну, что слышишь, Матвей Иванович?<br>
  <br>
  — Слышу какой-то шум, похожий на крик птиц, — сказал, приподнимаясь, Платов.<br>
  <br>
  — Да разве птица кричит в темную ночь? Она сидит смирно, — сказал старый донец.<br>
  <br>
  — Так что же это такое? — спросил Платов.<br>
  <br>
  — А вот что. Неприятель недалеко. Он стал лагерем, разложил огни, на свет поднялась 
  птица и кричит. По большому крику надо полагать, что огней много, значит много 
  и басурман. Теперь нужно держать ухо востро и ждать на заре нападения. Поживешь, 
  Матвей Иванович, довольно — узнаешь и больше.<br>
  <br>
  Платов выслушал слова сметливого казака, тихо прошел в лагерь, поднял свой полк, 
  окопался, составил повозки внутрь своего бивака и стал ждать нападения. На рассвете 
  появилась орда. Девлет-Гирей с 20000 всадников надвигался на полки Платова и 
  Ларионова, окопавшиеся в степи. Послали двух казаков с донесением Бухвостову. 
  Один тут же был убит, другой ускакал благополучно.<br>
  <br>
  Поднявшееся солнце осветило пеструю орду татарскую. Красные и белые чалмы, пестрые 
  куртки татар цветным ковром облегли казачий лагерь. Среди этой толпы серебряными 
  искрами сверкали панцири, сделанные из стальных цепочек, кавказских рыцарей 
  из Кабарды. Они гарцевали на легких лошадях подле самых окопов, метали стрелы 
  и пронзительно кричали. Все поле было покрыто всадниками.<br>
  <br>
  Ларионов был старше Платова, но Платов, видя колебания товарища, взял командование 
  на себя и решил отбиться от неприятеля во что бы то ни стало. Семь раз атаковали 
  татары лагерь Платова и семь раз две его пушки и дружные залпы казачьих ружей 
  отбивали их натиск. Много полегло казаков за валами, многие были изранены; укрепление 
  было разбито в нескольких местах, повозки поломаны. Треть лошадей, стоявших 
  в середине окопа, были перебита. Отчаяние охватило казаков. Патронов было мало, 
  солнце наступившего дня пекло невыносимо, нечем было утолить жажду, и помощь 
  не шла ниоткуда.<br>
  <br>
  Задумчивый и печальный стоял при своем полку полковник Ларионов. Вдруг он подошел 
  к Платову.<br>
  <br>
  — Матвей Иванович, — тихо сказал он, — нам придется сдаться. Сопротивление бесполезно. 
  Мы зря погубим казаков.<br>
  <br>
  — Нет! — решительно сказал молодой полковник. — Пускай лучше я умру с честью 
  и славою, чем отдамся врагу на поругание, к стыду моего отечества. Что будет, 
  то будет. Я надеюсь на Бога. Он не оставит нас без помощи!<br>
  <br>
  И снова казаки стали заряжать ружья и выстрелами отбивать приближавшихся татар. 
  И вдруг раздался радостный крик:<br>
  <br>
  — Пыль вдали! Это наши.<br>
  <br>
  И, действительно, вдали показалась колонна. Вот передние сдержали скок своих 
  лошадей, перевели их на рысь, вот задние надвинулись и широкая казачья лава 
  развернулась и понеслась на татар. Это был полк Уварова.<br>
  <br>
  &quot;На коней!&quot; — крикнул воодушевленным голосом Платов — и его казаки 
  и казаки Ларионова выскочили из укрепления и бросились на татар. Атакованные 
  с двух сторон казаками татары кинулись наутек, в степь. Казаки их преследовали. 
  Так скакали татары пять верст, когда неожиданно налетели на гусарский полк Бухвостова, 
  принявший их в шашки. Все поле покрылось убитыми. Кабардинские лошади, лишившись 
  всадников, носились со ржанием по полю. Казаки отлавливали их.<br>
  <br>
  Победой над татарами на р. Калалах казаки были обязаны молодому своему герою 
  — Платову&quot;.</em><br>
  <br>
