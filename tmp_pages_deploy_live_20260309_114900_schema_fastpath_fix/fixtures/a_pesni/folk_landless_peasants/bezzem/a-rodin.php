	<br>
        <span class="text"> <strong>Федор Родин </strong><br>
        <br>
        <span class="header"><strong>БУРЛАЦКИЙ ФОЛЬКЛОР</strong><br>
        </span> <br>
        <em>Родин Ф.Н. Бурлачество в России. Историко-социологический очерк. М.: 
        Мысль, 1975. С. 205-217, 234.</em><br>
        <br>
        <br>
        Веками бурлаки служили незаменимой тяговой силой на водном транспорте, 
        доставляя ежегодно миллионы пудов всяких товаров в торговые и промышленные 
        центры страны. Бурлаки способствовали развитию торговли и промышленности, 
        совершенствованию судоходства, и вместе с тем их трудом обогащались русское 
        государство, помещики, купцы, судовладельцы, предприниматели. <br>
        <br>
        Постоянным спутником бурлака, облегчающим его тяжкий труд, когда каждому 
        приходилось тянуть лямкой по 250—300 пудов груза, была песня. В ней отражалась 
        безотрадная жизнь народа, его судьба, его настрой. Были и озорные песни, 
        веселые запевки, шутки, прибаутки, подбадривавшие бурлаков своим мажорным 
        тоном. <br>
        <br>
        Песня помогала бурлакам соблюдать в движении общий ритм, становилась организующей 
        силой. Утомленная, зачастую полусонная вереница бурлаков, двигаясь в такт 
        мелодии, превращалась в дружный трудовой коллектив с единой волей. Даже 
        такие восклицания, как «Ай, да ой, да эй», вырывавшиеся из сдавленной 
        груди, приносили бурлакам облегчение, помогали преодолеть усталость, чтобы 
        всем вместе сделать новые и новые трудовые усилия.<br>
        <br>
        До появления парового флота в Поволжье бытовало множество трудовых песен, 
        создателями и исполнителями которых являлись сотни тысяч людей разных 
        национальностей и социальных групп, собиравшихся на речной транспорт со 
        всей территории Европейской России. Все трудовые песни, в том числе и 
        известная «Дубинушка», возникали как плод коллективного творчества. <br>
        <br>
        Бурлаки приспосабливали лирические, плясовые, шуточные народные песни 
        под свой «топ» — мерный шаг артели. Запевала чаще всего избирал песни 
        с ритмичным складом, легким и шуточным содержанием. «Брала девка лен», 
        «За двором, двором», «Заросла моя полосонька», «Как на зорьке, на заре, 
        по утренней росе», «Во лузях», «Как по морю, морю синему» и много, много 
        других. Зачинались они по условной команде «шишки»<sup>1</sup>:<br>
        <br>
        Брала девка лён, лён, <br>
        Брала, набирала, <br>
        Землю не сбивала. <br>
        Боялася девка, <br>
        Боялася красная <br>
        Да серого волка... <br>
        Не того волка боялась,<br>
        Кой по лесу бродит, <br>
        Сам голосом воет, <br>
        А того волка боялась, <br>
        Кой по полю рыщет, <br>
        Соловушкой свищет, <br>
        Красных девиц ищет. <br>
        <br>
        А вот шуточная ходовая песня, записанная в 1924 году в селе Промзино Сибирской 
        губернии со слов крестьянина И. К. Потехина, ходившего в молодости на 
        бурлацкий промысел. Эту песню бурлаки. любили за простоту запева и разнообразие 
        припевов. Вначале муж предлагает жене купить курочку, потом другую живность: 
        <br>
        <br>
        Пойдем-ка, женушка, домик наживать, <br>
        Пойдем-ка, размилая, в гостиный ряд гулять, <br>
        Купим мы, женушка, курочку себе. <br>
        <br>
        Припев: <br>
        <br>
        Курочка по семечку семечко клюет, <br>
        Клюет, клюет, поклёвывает. <br>
        <br>
        Далее третья строка запева каждый раз меняется: <br>
        <br>
        ...Купим мы, женушка, уточку себе. <br>
        ...Купим мы, женушка, индюшечку себе. <br>
        ...Купим мы, женушка, барашка себе. <br>
        ...Купим мы, женушка, гусоньку себе. <br>
        ...Купим мы, женушка, козыньку себе. <br>
        ...Купим мы, женушка, телёночка себе. <br>
        <br>
        А когда исполнялся припев, над рекой гулко раздавалось кряканье утки, 
        блеяние барашка, забавное подражание голосам домашних животных и птиц. 
        После каждого куплета припев соответственно наращивался новой первой строкой 
        и к концу песни удлинялся до восьми строк.<br>
        <br>
        Припев: <br>
        <br>
        Телёночек — мыки, мыки, <br>
        Козлёночек — шики, брики, <br>
        Гусонька — гаи, гаи, <br>
        Барашек — шадры, бадры, <br>
        Индюшечка — шулды, булды, <br>
        Уточка — с носка плеска, <br>
        Курочка по семечку клюет, <br>
        Клюет, клюет, поклёвывает. <br>
        <br>
        Тот же И. К. Потехин рассказал о распространенной у волжских бурлаков 
        ходовой песне «Про волика и козлика». При ее исполнении запевала размахивал 
        «манишкой» — полотенцем, веселя усталых рабочих. Вот ее текст в сокращенном 
        виде: <br>
        <br>
        Пойду я, молодёшенька, ко первому братцу. <br>
        Дай мне, братец, вола и козла. <br>
        <br>
        Припев: <br>
        <br>
        Был у молодёшеньки волик и козлик, <br>
        Был да ушёл, ушёл, ушёл, ушел. <br>
        <br>
        Пойду я, молодёшенька, ко второму братцу. <br>
        Дай мне, братец, вола и козла. <br>
        <br>
        Припев: <br>
        <br>
        Были у молодёшеньки два вола, два козла, <br>
        Были да ушли, ушли, ушли, ушли. <br>
        <br>
        Оказывается, молодешенька имела десять братьев, каждый давал ей по волу 
        и козлу, но все от нее уходили. Тогда молодешенька вновь шла к первому 
        братцу, песню начинали сначала...<br>
        <br>
        Широко использовалась бурлаками и народная плясовая песня «Во лузях». 
        Этот вариант записан в селе Савро-Мамышево Сосновской волости Рязанской 
        губернии в 1927 году со слов бывшего бурлака Ф. В. Борисова. Ее любили 
        за ритмичность, позволяющую двигаться не сбиваясь с ноги, мерным шагом. 
        <br>
        <br>
        Во лузях, во лузях, <br>
        Таки лузях, зеленых во лузях (2 раза) <br>
        Выросла, выросла, <br>
        Выросла трава шелковая (2 раза), <br>
        Расцвели, расцвели, <br>
        Расцвели цветы лазоревые (2 раза). <br>
        В той траве, в той траве, <br>
        И я в той траве выкормлю коня (2 раза), <br>
        Выкормлю, выкормлю, <br>
        И я выкормлю, выглажу его (2 раза). <br>
        Поведу, поведу, <br>
        Поведу я коня к батюшке (2 раза), <br>
        Уж ты, батюшка, родимый мой (2 раза), <br>
        Ты прими, ты прими, <br>
        Ты прими слово ласковое (2 раза), <br>
        Полюби, полюби, <br>
        Полюби слово приветливое (2 раза) ... и т. д. <br>
        <br>
        По словам Ф. В. Борисова, иногда шишка брал песню, имевшую острый сюжет, 
        чтобы отвлечь бурлаков от мрачных дум. К такому жанру относится ходовая 
        песня «Уговаривал Ваня Дуняшу». Пелась она с подъемом и имела большое 
        распространение. <br>
        <br>
        Уговаривал да друг Ванюшка <br>
        К себе Дуню ночевать.<br>
        Ночуй, ночуй, Дунюшка, <br>
        Ты ночуй, голубушка. <br>
        Есть ночуешь у меня, <br>
        Подарю, радость, тебя, <br>
        Подарю, Дуня, сережечки серебряные,<br>
        А другие — золотые со подвесочками.<br>
        На то Дуня соглашалась, <br>
        Ночевать с Ваней осталась. <br>
        Ложилася Дуня спать <br>
        На Иванову кровать. <br>
        Ей немножечко спалось, <br>
        Много виделось: <br>
        Будто Ванюшка пришел, <br>
        Друг великий подошел, <br>
        Он из горенки во горенку. похаживает... <br>
        <br>
        80-летний крестьянин из деревни Юрьево Спасского уезда Рязанской губернии 
        С. И. Зиняков рассказывал нам в 1927 году, что, когда судно вели против 
        течения подачей, употреблялись большей частью запевки, например: <br>
        <br>
        Вот пошли да повели, <br>
        Правой-левой заступили. <br>
        <br>
        Или: <br>
        <br>
        Ой раз, ой раз, <br>
        Еще разик, еще раз. <br>
        <br>
        Движение бурлаков по палубе от носа к корме устанавливалось такой запевкой: 
        <br>
        <br>
        Стукнем, брякнем об палубу, <br>
        Чтобы лямку натянули. <br>
        <br>
        И артель подхватывала: <br>
        <br>
        Валяй наша, валяй, <br>
        Вот поваливай, валяй. <br>
        <br>
        На лодках, завозивших вперед якорь, бурлаки под «гребь» весел пели (эта 
        песня была записана со слов другого рязанского крестьянина, Т. И. Шапкина, 
        в деревне Забелино Гиблицкой волости): <br>
        <br>
        Не качать ли нам березу, <br>
        Не валять ли нам кудряву.<br>
        Березонька белая, зелено-кудрявая, <br>
        Через речку свалена. <br>
        Тут и речка, тут и мост, <br>
        Через речку перевоз. <br>
        Парень девку перенес, <br>
        Перенес, перетащил, <br>
        Ничего не намочил, <br>
        Лишь башмак с ноги сскочпл. <br>
        — Мне не жалко башмачка, <br>
        Жалко белого чулка: <br>
        Башмак батюшка купил, <br>
        Чулок милый подарил... <br>
        <br>
        На гребных грузовых и пассажирских судах часто словесная команда заменялась 
        для поддержания такта и ритма не только песней, но и ударами молота о 
        чугунную доску, хлопаньем в ладоши, посвистом. <br>
        <br>
        Бывшие бурлаки-коноводы Пряхин и Трусов из той же деревни Забелино рассказывали, 
        что при тяге судов лошадьми погонщики применяли условный крик и свист. 
        Перед тем как двинуться в путь, носовой обычно кричал «шишке» на берег: 
        <br>
        <br>
        — Эй, малыш, разворачивай! <br>
        <br>
        Чтобы ускорить ход, раздавалась команда: <br>
        <br>
        — Ровняй! Ровняй! <br>
        <br>
        Если лошади слишком удалялись от воды, с судна кричали: <br>
        <br>
        — Ниже! Ниже! <br>
        <br>
        А если слишком приближались к воде кричали: <br>
        <br>
        — Выше! Выше! <br>
        <br>
        Когда требовалось остановить лошадей, по реке неслось звонкое: <br>
        <br>
        — Коренная, ло-ло-ло! <br>
        <br>
        Если судно вставало на мель, бурлаки подводили под днище «стяги», нажимая 
        на них плечами, по пояс в воде, жалобно вскрикивали:<br>
        <br>
        — Ух-да! Ух-да! <br>
        <br>
        Снявшись с мели, лоцман кричал коноводам на берег: <br>
        <br>
        — Поднимай легонько, эй, поднимай! Поднимай! <br>
        <br>
        В ответ раздавалось хлопанье кнутов и резкий свист. В такие минуты на 
        реке, особенно на перекатах, где скапливалось много судов, стоял невообразимый 
        шум от истошных криков и свиста. <br>
        <br>
        Много разных запевок использовали бурлаки при стаскивании судов с мели, 
        при подъеме и передвижении тяжестей, в борьбе со встречными ветрами и 
        в других случаях. Запевки обычно создавались непосредственно в процессе 
        труда. Они были крайне просты и состояли обычно из одного-двух коротких 
        слов, повторяющихся без конца, переходя как бы в песню без слов. <br>
        <br>
        Когда нужно было стащить судно с мели, артель сиплыми голосами выводила: 
        <br>
        <br>
        — Нейдет! Нейдет! Ау, да ух! <br>
        Ой раз, ой раз! <br>
        Еще разик, еще раз! <br>
        Сорв<strong>а</strong>ли — сорвал<strong>и</strong>, <br>
        Сорв<strong>а</strong>ли — сорвал<strong>и</strong>! <br>
        <br>
        Лоцман, подбодряя усталых бурлаков, кричал:<br>
        <br>
        — Пошло батюшки! Пошло ходом! <br>
        <br>
        Тогда, собираясь с силами, бурлаки меняли старую погудку на новую: <br>
        <br>
        Ведь пошел-таки, пошел, <br>
        Он пошел, да и пошел! <br>
        Он и ходом, ходом, ходом, <br>
        Ходом па ходу пошел! <br>
        <br>
        Раздавалась и при съеме судна с мели и знаменитая «Дубинушка» в бесконечном 
        множестве вариантов: <br>
        <br>
        Ой, дубинушка, ухни! <br>
        Ой, зеленая, подерни! <br>
        Подерни, подерни! <br>
        Да ух! <br>
        Ой, суденышко встало, <br>
        Видно, силушки не стало! <br>
        Ой ребята, собираться, <br>
        За веревочку хвататься! <br>
        Ой, дубинушка, ухнем! <br>
        Ой, зеленая, сама идет! <br>
        Ухнем! <br>
        <br>
        Но вот судно тронулось с мели, и бурлаки бойкими, веселыми голосами гомонили: 
        <br>
        <br>
        ...Раззеленая сама пойдет! <br>
        Идет, идет, сама пойдет,<br>
        Идет, идет, сама пойдет! <br>
        <br>
        При благополучном движении судна песня повторялась в ускоряющемся темпе. 
        <br>
        <br>
        Иные песни звучали во время сильных встречных ветров, когда судно тянуло 
        назад. Тогда затягивалась жалобная запевка: <br>
        <br>
        Эх, матушка-Волга!<br>
        Широка и долга,<br>
        Укачала, уваляла,<br>
        У нас силушки не стало!<br>
        Эх, матушка-Клязьма! <br>
        Широка и грязна. <br>
        Укачала валяла,<br>
