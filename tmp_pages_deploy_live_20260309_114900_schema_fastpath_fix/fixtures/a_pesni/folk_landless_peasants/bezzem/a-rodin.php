        I, когда сооружался трудом крепостных крестьян Ладожский канал. Для его 
        эксплуатации потребовалось большое количество опытных работников, по уже 
        не подневольных. Вербовали их главным образом в низовьях Волги, из тамошних 
        бурлаков, о чем и поведала песня<sup>5</sup>: <br>
        <br>
        Поутру то было раным-рано, <br>
        На заре то было на утренней, <br>
        На восходе красного солнышка. <br>
        Что не гуси, братцы, не лебеди <br>
        Со лузей, озер поднималися, <br>
        Поднималися добры молодцы, <br>
        Добры молодцы, люди вольные, <br>
        Все бурлаки понизовые, <br>
        На канавушку на Ладожску, <br>
        На работушку государеву... <br>
        <br>
        Примерно к тем же временам, может быть к чуть более поздним, принадлежит 
        и жалобное стенание бурлака. Его «вид на жительство» остался у хозяина-нанимателя, 
        а за побег с судна грозит суровая кара, скорее всего каторга. Безысходная 
        судьба горемыки отражена в этих поэтических напевах<sup>6</sup>: <br>
        <br>
        Не былинушка в чистом поле зашаталася, <br>
        Зашаталася бесприютная моя головушка, <br>
        Бесприютная моя головушка молодецкая. <br>
        Уж куда-то я, добрый молодец, ни кинуся, <br>
        Что по лесам, по деревням все заставы, <br>
        На заставах ли все крепкие караулы, <br>
        Они спрашивают негодного пашпорта, <br>
        Что за красной печатью сургучёвой. <br>
        У меня ль, у добра молодца, своеручный, <br>
        Что на тоненькой, на белой, на бумажке. <br>
        Что куда ни пойду, братцы, ни поеду, <br>
        Что ни в чем-то мне, молодцу, нет счастия... <br>
        <br>
        На первом Всесоюзном съезде советских писателей А.М. Горький сказал, что 
        подлинную историю трудового народа «нельзя знать, не зная историю народного 
        творчества». Бурлацкий фольклор — это и есть живая история творчества 
        большого отряда трудового народа — волгарей. Прямо или косвенно он использован 
        во многих произведениях А.Н. Некрасова, А.Н. Островского, В.Г. Короленко, 
        П.И. Мельникова-Печерского, в замечательных полотнах О.А. Васильева, И.Е. 
        Репина, И.И. Левитана, посвященных великой труженице Волге, ее людям, 
        бесконечному разнообразию ее великолепных пейзажей.<br>
        <br>
        Бережное отношение к народному творчеству, к истории трудящихся масс — 
        характерная черта искусства социалистического реализма. Советские писатели, 
        поэты, художники постоянно обращаются к этому неисчерпаемому источнику 
        творческого вдохновения. Знание истории трудового народа, его жизни и 
        быта в прошлом помогает нынешним поколениям полнее и лучше оценить смысл 
        и значение великих социалистических преобразований, масштабы свершений 
        тружеников нашей Советской Отчизны во имя строительства коммунизма.<br>
