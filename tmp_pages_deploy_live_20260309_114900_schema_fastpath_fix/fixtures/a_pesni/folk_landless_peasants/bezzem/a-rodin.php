        <br>
        По словам А. М. Горького, Лев Николаевич Толстой отмечал, что «поэзия 
        — безыскусственна. ...Мужик тоже не знает, что он поет, — ох, да-ой, да-эй 
        — а выходит настоящая песня, прямо из души»<sup>2</sup>. Эту же мысль 
        высказывает в своих очерках «Ветлужские бурлаки» Н. Оглоблин. Он не раз 
