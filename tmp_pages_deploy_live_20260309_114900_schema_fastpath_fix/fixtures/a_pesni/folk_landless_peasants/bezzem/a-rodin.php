        рабочие поднимают на беляне огромное, тяжелое бревно, чтобы скатить его 
        по проложенным «сливкам» на берег. Один из них певуче тянет: «Еще вы-ы-ше, 
        еще вы-ы-ше», ему вторит другой: «Еще выше». Остальные подхватывают и 
        дружными усилиями поднимают бревно на катки, по которым оно и слетает 
        с беляны. <br>
