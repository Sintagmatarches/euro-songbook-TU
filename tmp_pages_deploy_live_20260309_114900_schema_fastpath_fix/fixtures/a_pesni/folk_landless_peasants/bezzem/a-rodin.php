        бурлак, сидя в сумерки на тихо плывшем плоту, заливался горькими слезами 
        и нескончаемо тянул: <br>
        <br>
        Ай, маменька, прости! <br>
        Ай, родимая, прости! <br>
        <br>
        В повторении этих несложных слов чувствовалась неподдельная грусть и тяжелая 
        душевная драма. В дикий вой и выкрикивания «Ай-ай-ай! Ой-ой-ой! Эх!» бурлаки 
        действительно вкладывают все свое горе, всю муку и тоску<sup>2</sup>. 
        <br>
        <br>
        В 90-х годах учитель А. Кондратьев в книге об известном певце Д. А. Агреневе-Славянском 
        писал, что бурлацкие песни оказали большое влияние на музыкальное творчество 
        Х1Х века. Бурлацкой песней «Эй, ухнем!» восхищалась пе только Россия, 
        но и вся Западная Европа. Творец оперы «Фауст» Шарль Франсуа Гуно оценивал 
        ее чрезвычайно высоко. «Скорее я выпил бы всю воду в Волге, чем охладел 
        бы к чудным звукам этой русской песни, — говорил Гуно. — А кажется, что 
        в ней особенного? Она так бедна разнообразием звуков и, пожалуй, еще беднее 
        своим содержанием: «Эй, ухнем, ай, да, да, ай, да, разовьем мы березу, 
        разовьем мы кудряву...» Вот и все ее бесконечно повторяющееся содержание. 
        Да и песня ли это? Нет, это не песня, это «стон наболевшей души»<sup>3</sup>.<br>
        <br>
        На каждый случай, на каждый отдельный рабочий прием у бурлаков находились 
        самые простые слова и песни, без конца повторявшиеся на один и тот же 
        однообразный мотив. Скорее это даже пе песни, а просто певучие восклицания 
