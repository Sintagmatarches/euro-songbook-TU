	<span class="header"><strong>АХ ТЫ ДУШЕЧКА, ЖЕНА МОЯ МОЛОДАЯ...</strong></span><br>
	<br>«Ах ты душечка, жена моя 
  молодая, <br>
  Что на улице застоялась, <br>
  На приказ, мой свет, загляделась, <br>
  На приказное на окошко?» <br>
  — «И ты душечка, мой сердечный друг, <br>
  Я на улице застоялася, <br>
  На приказ, мой свет, загляделася: <br>
  Во приказе в государевом, <br>
  Что во той ли темной темнице, <br>
  За решеткою за железною <br>
  Сидит душечка добрый молодец. <br>
  И он пишет себе грамотку, <br>
  Не пером пишет, не чернилами – <br>
  Он своими горючими слезьими. <br>
  Не ко батюшке, не ко матушке – <br>
  Он ко душечке к молодой жене: <br>
  «Ах ты душечка, молода жена, <br>
  Продавай свое житье-бытье, <br>
  Выкупай меня из неволюшки, <br>
  Как из той ли темной темницы! <br>
  Буде мало тебе всего покажется — <br>
  В кабалу отдай малых детушек!» <br>
  На ответ пишет молода жена: <br>
  «Ах ты душенька, мой сердечный друг, <br>
  Не продам своего житья-бытья, <br>
  Не отдам в кабалу малых детушек! <br>
  Уж и так тебя бог помилует, <br>
  Государь тебя царь пожалует» <br>
  Как далече, далече во чистом поле<br>
  Стоят два столбичка высокие, <br>
  Перекладинка лежит кленовая <br>
  И петелька лежит шелковая...<br>
  «Уж как я тебе, друг, говорила, <br>
  Честью-лестью вас упрашивала: <br>
  Не седлай коня поздно вечером, <br>
  Не съезжай с двора пополуночи, <br>
  Не вози платье кровавое! <br>
  Уж как ты, мой друг, меня не послушался, <br>
  Ты седлал коня поздно вечером, <br>
  Ты съезжал со двора пополуночи, <br>
  Привозил платье кровавое; <br>
  Ты за то сидишь в темной темнице, <br>
  Не считай меня своей женой!» </font></p>
