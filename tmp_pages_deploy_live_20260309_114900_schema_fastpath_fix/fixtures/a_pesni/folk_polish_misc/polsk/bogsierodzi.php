  Autorem tekstu kolędy <em>Bóg się rodzi</em> jest Franciszek Karpiński (1741—1825), 
  poeta, przedstawiciel sentymentalizmu. Tworzył liryki miłosne (m.in. <em>Laura 
  i Filon</em>), sielanki, elegie, а także teksty pieśni religijnych, wśród nich 
  popularną do dnia dzisiejszego pieśń <em>Kiedy ranne wstają zorze. </em><br>
  <br>
  Melodia tej kolędy, utrzymana w rytmie poloneza, powstała w XVIII wieku. Nieznany 
  jest jej twórca. Najstarszy jej zapis pochodzi z przełomu XVIII i XIX wieku. 
