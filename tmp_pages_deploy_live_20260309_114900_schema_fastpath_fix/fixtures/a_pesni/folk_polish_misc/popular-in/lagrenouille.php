  <br>
  Un garçon part en vadrouille.<br>
  Au bord d'un étang<br>
  Il attrape une grenouille<br>
  Qui dit en tremblant:<br>
  &quot;Laisse-moi m'en aller<br>
  Et je te promets<br>
  De réaliser<br>
  Trois de tes souhaits.<br>
  <br>
  Fais un vœu, mon bonhomme,<br>
  Car je peux, mon bonhomme,<br>
  Si tu veux, mon bonhomme,<br>
  Te donner le bonheur&quot;.<br>
  <br>
  Tout surpris par ce langage<br>
  Il lui dit: &quot;Je veux<br>
  Beaucoup d'or dans mes bagages,<br>
  Des habits soyeux&quot;.<br>
  Mais au bout d'un mois<br>
  Il revint la voir<br>
  Et lui demanda<br>
  &quot;Donnes-moi la gloire!&quot;<br>
  <br>
  &quot;Ce vœu-là mon, bonhomme,<br>
  Tu l'auras, mon bonhomme,<br>
  Mais cela, mon bonhomme,<br>
  N'est pas le vrai bonheur&quot;.<br>
  <br>
  De tous les puissants sur terre<br>
  Il devint le roi<br>
  Mais dans son cœur solitaire<br>
  Y avait plus de joie.<br>
  Il revint un jour<br>
  Triste et malheureux;<br>
  &quot;Donne-moi l'amour<br>
  C'est mon dernier vœu!&quot;<br>
  <br>
  &quot;Ce vœu là, mon bonhomme,<br>
  Tu l'auras, mon bonhomme,<br>
  Et cela, mon bonhomme,<br>
  Sera le vrai bonheur&quot;.<br>
  <br>
  Il sort une plainte étrange.<br>
  La grenouille alors<br>
  Devant lui soudain se change<br>
  En fille aux cheveux d'or.<br>
  La main dans la main<br>
  Ils s'en sont allés<br>
  Et sur leur chemin<br>
  Les oiseaux sifflaient<br>
  <br>
  &quot;Sois heureux, mon bohomme,<br>
  Car un vœu, mon bonhomme,<br>
  Pour toujours, mon bonhomme,<br>
  T'a donné le bonheur&quot;.<br>
