  <br>
  <strong>Франсис Лемарк</strong> [1917-2002], автор и исполнитель песен, начал 
  выступать в конце тридцатых годов - он дебютировал с прославленными певцами 
  Эдит Пиаф и Ивом Монтаном. Со временем Лемарк написал музыку ко многим французским 
  фильмам, но особенную известность принесли ему песни <em>La grenouille</em> 
  и <em>Quand un soldat...</em> Последняя вместе с <em>La colombe</em> Бреля и 
  <em>Le déserteur</em> Виана вошла в золотой фонд политической песни послевоенной 
  Франции. <br>
