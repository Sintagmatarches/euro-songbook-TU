	  und kehr in meinen Stand zurück;<br>
	  doch das geh ich mit Freuden ein:<br>
	  halb Sultan und halb Papst zu sein.<br>
	<br>6. Drum Mädchen, gib mir einen Kuß,<br>
	  denn jetzt bin ich dein Sultanus!<br>
	  Ihr trauten Brüder, schenket ein,<br>
