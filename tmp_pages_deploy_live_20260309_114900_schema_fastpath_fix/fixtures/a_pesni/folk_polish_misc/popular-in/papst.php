	  ein holdes Mädchen küßt ihn nicht;<br>
	  er schläft in seinem Bett allein:<br>
	  drum möchte ich der Papst nicht sein.<br>
	<br>3. Der Sultan lebt in Saus und Braus,<br>
	  er wohnt in einem Freudenhaus<br>
	  voll wunderschönen Mägdelein:<br>
	  drum möcht ich wohl der Sultan sein.<br>
