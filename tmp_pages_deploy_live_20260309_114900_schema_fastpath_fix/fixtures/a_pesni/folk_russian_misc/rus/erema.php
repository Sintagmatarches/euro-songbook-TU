    Извозчичек</div>
<br>
Ехал, ехал раз извозчичек,<br>
Ехал, ехал, да взгрустнул.<br>
А слеза его горючая<br>
Покатилась на щеку.<br>
Со щеки-то соскочила<br>
И упала на кафтан.<br>
С кафтана-то соскочила<br>
И упала на портки.<br>
А с портков-то соскочила<br>
Прямо в валеный сапог.<br>
Чрез сапог-то просочилась<br>
И упала на песок.<br>
На песке она лежала,<br>
Пока дворник не подмел.<br>
Дворник в красной был рубахе,<br>
Брюки писаны змеей.<br>
Сапоги-то с бацацирой,<br>
А калоши с ремяшком.<br>
А невеста-то в деревне<br>
И зовут его Ерем.<br>
<br>
<div class="i">Коваль М. Извозчичек. [Слова народные. Муз. Мариана Коваля.] М.: Музсектор Госиздата,
    1930 (Всероссийское рабочее музыкальное об-во «Музыка массам», № 7)</div>
