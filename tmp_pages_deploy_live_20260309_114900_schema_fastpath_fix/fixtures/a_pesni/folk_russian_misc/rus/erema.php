<h1>СТРАДАНИЯ ИЗВОЗЧИКА ЕРЕМЫ</h1>

  А как на речке было на Фонтанке,<br>
  Стоял извозчик, стало быть, молодой.<br>
  А он был в розовой, стало быть, рубашке<br>
  И в сятюльевых, стало быть, штанах.<br>
  А набекрень была его фуражка,<br>
  А в кармане черствы пирожки.<br>
  Да вдруг изво-, извозчичку взгрустнулось,<br>
  За-, залился он горькою слезой.<br>
  А слеза была, стало быть, горюча,<br>
  По-, покатилась прямо на кафтан.<br>
  С кафтана слеза, стало быть, скатилась<br>
  И упала прямо на портки.<br>
  А с порток слеза, стало быть, скатилась<br>
  И упала в валяный сапог.<br>
  Чрез подметку она просочилась<br>
  И упала прямо на песок.<br>
  А на песке слеза, стало быть, лежала,<br>
  По-, пока дворник да, стало быть, не подмел.<br>
  А невеста у него в деревне,<br>
  А зовут его, стало быть, Ерема.<br>
    <br>
<div class="i">С фонограммы Жанны
  Бичевской, альбом «Старые русские народные деревенские и городские песни и баллады», 
    Часть 2, ZeKo Records, 1996</div>
<br>
<br>
Песня сложена народным 4-стопным цезурированным тактовиком или стилизована под него. Если верно первое, то исполнители и издатели
выравнивали песню в литературную силлабо-тонику (у Бичевской это 5-стопный двусложник, у <a href="../bard/vizbor/sleza.php">Юрия Визбора</a> -
5-стопный ямб, а в нотном издании Мариана Коваля - 4-стопный хорей). Если верно второе, то они, наоборот, расшатывали
силлабо-тонику для усиления народного колорита;
вероятно, так оно и было, и первоначальная песня сложена 4-стопным хореем, наиболее типичным размером стилизаций под народные песни
в дешевых песенниках 1900-1910-х гг.<br>
<br>
Издавалась на грампластинке: Извозчичек. Сл. и муз. народные. Арт. Л.А. Русланова. Н.И. Луковихин (балалайка),
Н.В.Садовников (баян) и С.О.Максаков (концертино). Грампласттрест, Ногинский завод.
Каталожный № 10123. Дата и место записи: 1940, Москва.<br>
