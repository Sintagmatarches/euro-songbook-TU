<br>
        Кубань становилась прибежищем вольных казаков, когда репрессии царских 
        властей обрушились на традиционные казачьи центры - Дон и Запорожскую 
        Сечь. Осенью 1708 года на Кубань ушли &quot;сорок тысяч&quot; казаков 
        атамана Игната Некрасова, не пожелавшие сложить оружия после разгрома 
        восстания Кондратия Булавина. Некрасовцы вместе с беглыми раскольниками 
        создали вольное Войско Кубанское и держались на Кубани до 1741 года, сохраняя 
        свои древние обычаи. Затем вынуждены были отступить на Дунай, в турецкие 
        владения. <br>
		<br>
        В 1775 году войска Екатерины II уничтожили Запорожскую Сечь, и 12 тысяч 
        непокорных запорожцев ушли на своих чайках на Кубань, в то время принадлежавшую 
        Турции. Оставшиеся подлежали расказачиванию, часть обратили в крепостных, 
        однако в 1787 году вспыхнула очередная турецкая война, в которой ощутилась 
        явная нехватка запорожцев. Из казаков, согласившихся перейти на царскую 
        службу, в том же году было сформировано Черноморское казачье войско (Войско 
        Верных казаков Черноморских).<br>
    <br>
        В 1791-92 годах началось насильственное официальное переселение на Кубань 
        черноморских, части донских, а затем и екатеринославских казаков (при 
        этом недолго просуществовавшее Екатеринославское войско упразднили, влив 
        его в Черноморское). Из терцев и переселенных донцов в 1832 году образовали 
        Кавказское линейное казачье войско. В 1860 году из него выделили терцев, 
        возродив Терское казачье войско, а основную часть объединили с Черноморским 
        войском в Кубанское казачье войско. <br>
    <br>
    На Кубани бытуют две независимые песенные казачьи традиции: русская линейная, 
    восходящая к донской, и украинская черноморская, наследница Запорожской Сечи.<br>
