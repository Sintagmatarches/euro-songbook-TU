<br><br>
<strong>Ноябрьское восстание 1830-1831 гг. в Польше <br>
(Powstanie Listopadowe) </strong><br>
<br>
В 1829-30 гг. по Европе прокатилась волна восстаний: в 1829 г. Греция добилась автономии от Турции, 1830 г. - июльская революция в Париже и бельгийское восстание против голландского господства. <br>
<br>
29 ноября 1830 г. в Варшаве группа повстанцев из Школы пехотных подхорунжих во главе с подпрапорщиком Петром Высоцким атаковала резиденцию наместника Польши великого князя Константина Романова, но князю удалось бежать. В городе установилась диктатура генерала Юзефа Хлопицкого. 13 декабря 1830 г. сейм призвал к народному восстанию, однако Хлопицкий отказался подписать призыв. 25 января 1831 г. сейм объявил об отмене подчинения Польши русскому императору. <br>
<br>
Польша (та ее часть, что входила в состав Российской империи) имела на тот момент самостоятельную регулярную армию численностью 27 тыс. человек при 97 орудиях. После мобилизации армия выросла до 57 тыс. человек и 140 орудий. Российский император Николай I двинул против Польши армию в 115 тыс. человек при 336 орудиях. 26 мая 1831 г. польские войска были разбиты под Остролекой, от польской армии осталось 52 тыс.  6 сентября пала Варшава. Основная часть сохранившихся польских войск 5 октября ушла в Пруссию. До 9 октября сопротивлялся Модлин, до 21 октября - Замостье. После разгрома восстания началась "Великая эмиграция" - порядка 8-9 тыс. поляков покинули край. <br>
