  Первоначальный текст – стихотворение "Гимн на годовщину провозглашения Королевства 
  Польского" - сложил в 1816 г. известный поэт и драматург польского классицизма 
  Алоизий Фелиньский, в прошлом - секретарь Тадеуша Костюшко и участник его восстания. 
  Стихотворение было навеяно английским гимном "Боже, храни короля" и адресовалось 
  российскому императору Александру I, который носил титул и короля польского 
  - с момента провозглашения 20 июня 1815 г. по итогам Венского конгресса Королевства 
  Польского (в составе Российской империи). Поэт надеялся, что Александр I даст 
  Польше больше независимости. Опубликовано в том же году в журнале "Варшавский 
  дневник". Его рефрен гласил: "Пред твоим ликом несем мольбу, / Нашего короля 
  сохрани нам, Господь". <br>
  <br>
  По просьбе брата Александра I великого князя Константина, занимавшего пост главнокомандующего 
  Войска Польского, музыку в том же году написал подпоручик пехоты скрипач-виртуоз 
  Ян Непомуцен Кашевский, бывший участник наполеоновской кампании. Получилась 
  лояльно-монархическая песнь, названная "Национальная песнь о благополучии короля".<br>
  <br>
  Через год, опять же в журнале "Варшавский дневник", поэт Антоний Горецкий, в 
  будущем – один из организаторов Ноябрьского восстания в Литве, опубликовал в 
  противовес стиху Фелиньского "Гимн Богу о сохранении вольности" (1817). Мелодию 
  к нему написал Кароль Курпиньский – будущий автор музыки <a href="warsz1831.htm">"Варшавянки 
  1831 года"</a>.<br>
  <br>
  Накануне Ноябрьского восстания 1830-1831 гг. два куплета гимна Горецкого прибавили 
  к двум первым куплетам гимна Фелиньского – и такой четырехкуплетный гимн под 
  заглавием "Боже, что Польшу" вошел в изданный в Варшаве во время восстания cборник 
  стихов "Родные песни". Вторая строка рефрена гласила: "Нашу отчизну дай нам 
  вернуть, Господь". Мелодия Кашевского не устоялась, и песню стали исполнять 
  на анонимный мотив польского песнопения XVIII века "Здраствуй, Дева Мария".<br>
  <br>
  За 30 лет между Ноябрьским и Январским 1863-64 гг. восстаниями гимн дополнился 
  новыми куплетами - авторства Винцента Смагловского и других, неизвестных поэтов. 
  К 1860 году и в период Январского восстания в песне было уже 11 куплетов – они 
  и представлены выше. Во время восстания была необычайно популярна.<br>
  <br>
  Современный рефрен <strong>"Вольную отчизну благослави, Господь"</strong> появился 
  после обретения в 1918 году независимости, а сама песня была одним и кандидатов 
  на роль государственного гимна. В годы гитлеровской оккупации и в послевоенной 
  Народной Польше на манифестациях протеста в рефрене пели: "Вольную отчизну дай 
  нам вернуть, Господь".<br>
  <br>
  В итоге, и в XIX, и XX вв. "Боже, что Польшу" была наряду с <a href="mazurdabr.htm">"Мазуркой 
  Домбровского"</a> главной национальной песней поляков.<br>
