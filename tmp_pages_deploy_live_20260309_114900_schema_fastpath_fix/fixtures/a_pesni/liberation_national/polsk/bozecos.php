  Alojzy FELIŃSKI (1771-1820), poeta i dramatopisarz, jeden z najwybitniejszych 
  przedstawicieli klasycyzmu w literaturze polskiej, uczęstnik powstania i sekretarz 
  Tadeusza Kościuszki. Jego tragedia <em>Barbara Radziwiłłówna</em> (1817) uważana 
  jest za czołowy utwór klasycystyczny dramaturgii polskiej. Jest też autorem 
  tekstów kilku znanych pieśni religijnych, m.in. pierwszej wersji tekstu <em>Boże, 
  coś Polskę</em> (<em>Hymn na rocznicę ogłoszenia Królestwa Polskiego</em>, 1816, 
  z muzyką J. Kraszewskiego, która niebawem została zastąpiona melodią polskiego 
  hejnału maryjnego z XVIII wieku <em>Bądź pozdrowiona Panienko Maryjo</em>, śpiewaną 
  do dziś). <br>
  <br>
  Antoni GORECKI (1787-1861), poeta, autor wierszy patriotycznych (dumy, elegie), 
  bajek i fraszek politycznych, przyjaciel Adama Mickiewicza, współorganizator 
  powstania listopadowego w Wilnie. Tekst jego <em>Hymnu do Boga о zachowaniu 
  wolności</em> (1817) stał się оkоłо 1830 roku częścią pieśni narodowo-religijnej 
  <em>Boże, coś Poiskę. </em><br>
