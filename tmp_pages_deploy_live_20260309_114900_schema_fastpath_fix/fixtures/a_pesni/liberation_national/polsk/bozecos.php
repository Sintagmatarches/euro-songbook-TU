	<span class="header"><strong>Boże, coś Polskę <br>
  (Боже, что Польшу...)</strong><br></span>
  <br>
  Melodia anonimowa z XVIII wieku<br>
  Słowa: A. Feliński, A. Gorecki, W. Smagłowski i nieznani poeci<br>
  <br>
  Boże, coś Polskę przez tak liczne wieki<br>
  Otarczał blaskiem potęgi i chwały,<br>
  Coś ją osłaniał tarczą swej opieki<br>
  Od nieszczęść, które pognębić ją miały.<br>
  <br>
  Przed Twe ołtarze zanosim błaganie:<br>
  Ojczyznę wolną pobłogosław, Panie.<br>
  <br>
  Ту, który potem, tknięty jej upadkiem, <br>
  Walczących wspierał za najświętszą sprawę <br>
  I chcąc świat cały mieć jej męstwa świadkiem <br>
  W nieszczęściach samych pomnażał jej sławę. <br>
  <br>
  Przed Twe ołtarze... <br>
  <br>
  Niedawnoś wolność zabrał z polskiej ziemi, <br>
  А łez, krwi naszej popłynęły rzeki, <br>
  Jakże to musi bуć okropnie z tymi, <br>
  Którym ojczyznę odbierasz na wieki. <br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
  Wróć nowej Polsce świetność starożytną, <br>
  Użyźniaj pola, spustoszałe łany, <br>
  Niech szczęście, pokój na nowo zakwitną, <br>
  Przestań nas karać, Boże zagniewany! <br>
  <br>
  Przed Twe ołtarze... <br>
  <br>
  Boże, którego ramię sprawiedliwe <br>
  Żelazne berła władców świata kruszy, <br>
  Skarć naszych wrogów zamiary szkodliwe <br>
  Obudź nadzieją w każdej polskiej duszy.<br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
  Boże Najświętszy! przez Twe wielkie cudy <br>
  Oddalaj od nas klęski, mordy boju, <br>
  Połącz wolności węzłem Twoje ludy <br>
  Pod jedno berło Anioła Pokoju. <br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
  Boże Najświętszy! przez Chrystusa rany<br>
  Świeć wiekuiście nad braćmi zmarłymi. <br>
  Spojrzyj na lud Twój niewolą znękany, <br>
  Przyjmij ofiary z synów polskiej ziemi.<br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
  Boże Najświętszy, od którego woli <br>
  Istnienie świata całego zależy, <br>
  Wyrwij lud polski z tyranów niewoli, <br>
  Wspieraj szlachetne zamiary młodzieży.<br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
  Gdy naród polski dzisiaj we łzach tonie, <br>
  Za naszych braci poległych błagamy, <br>
  By ich męczeństwem uwieńczone skronie <br>
  Nam do wolności otworzyły bramy.<br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
  Jedno Twe słowo, wielki niebios Panie, <br>
  W chwili nas z prochów wskrzesić będzie zdolne. <br>
  А gdy zasłużym na Twe ukaranie, <br>
  Obróć nas w prochy, ale w prochy wolne!<br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
  Powstała z grobu na Twe władne słowo<br>
  Polska, wolności narodów chorąży. <br>
  Pierzchnęły straże, а ponad jej głową <br>
  Znowu swobodnie Orzeł Biały krąży!<br>
  <br>
  Przed Twe ołtarze...<br>
  <br>
