  Pieśń powstała w 1816 roku. Jej tekst napisał poeta i czołowy dramatopisarz 
  polskiego klasycyzmu, Alojzy Feliński, uczestnik insurekcji i sekretarz Tadeusza 
  Kościuszki. Tekst nosił tytuł <em>Hymn na rocznicę ogłoszenia Królestwa Polskiego</em> 
  i był inspirowany angielskim hymnem <em>God Save the King</em>, głosząc w refrenie 
  "Przed Twe oblicze zanosim błaganie, naszego króla zachowaj nam Panie&quot;. 
  Feliński napisał ten tekst na cześć Aleksandra I, cara rosyjskiego i od roku 
  1815 (w stworzonym 20 czerwca po kongresie wiedeńskim - Królestwie Polskim) 
  - również króla polskiego, z którego osobą poeta wiązał nadzieje na większe 
  usamodzielnienie Polski. <br>
  <br>
  Na zamówienie księcia Konstantego (brata Aleksandra I), który w roku 1816 bуł 
  naczelnym dowódcą wojska polskiego - podporucznik piechoty i skrzypek-wirtuoz 
  Jan Nepomucen Kaszewski (uczestnik kampanii napoleońskiej) napisał muzykę do 
  tekstu Felińskiego i w ten sposób powstała <em>Pieśń narodowa za pomyślność 
  Króla</em>, lojalistyczno-monarchistyczna, hołdownicza pieśń-modlitwa. Rok później, 
  na łamach "Pamiętnika Warszawskiego&quot; (tego samego czasopisma, w którym 
  po raz pierwszy w 1816 opublikowano tekst <em>Hymnu</em> Felińskiego) - poeta 
  Antoni Gorecki, autor wielu wierszy patriotycznych (późniejszy współorganizator 
  powstania listopadowego na Litwie), opublikował polemiczny w stosunku do wiersza 
  Felińskiego - <em>Hymn do Boga о zachowaniu wolności</em> (1817). Melodię do 
  tekstu Hymnu skomponował Karol Kurpiński i pieśń Goreckiego - Kurpińskiego funkcjonowała 
  jako samodzielny utwór. (Niektóre śpewniki przez to mylnie przypisywały Kurpińskiemu 
  autorstwo melodii do pieśni <em>Boże, coś Polskę.</em>)<br>
  <br>
  Dwie zwrotki z tekstu hymnu Goreckiego dołączono do dwóch pierwszych zwrotek 
  hymnu Felińskiego i podczas powstania listopadowego ukazał się w Warszawie zbiór 
  wierszy (wydany przez К. W. Wójcickiego) <em>Pieśni ojczyste</em> - zawierający 
  czterozwrotkowy hymn <em>Boże, coś Polskę</em> z refrenem "Naczą ojczyznę racz 
  nam wrócić, Panie&quot;. Melodia pierwotna J.N. Kaszewskiego uległa zapomnieniu, 
  а pieśń tę zaczęto śpiewać do anonimowej melodii polskiego hejnału maryjnego 
  z XVIII wieku <em>Bądź pozdrowiona Panienko Maryjo</em>. I z melodią tego hejnału, 
  hymn Boże, coś Polskę jest śpiewany do dnia dzisiejszego (jak również od połowy 
  XIX wieku na tę samą melodię zaczęto śpiewać inną pieśń maryjną - <em><a href="serdeczna.htm">Serdeczna 
  matko</a></em>).<br>
  <br>
  Niektórzy wskazują jako źródło melodii <em>Boże, coś Polskę</em> arię <em>Qu'on 
  soint jalouse</em> z opery Jean Pierre Soliego <em>Sekret </em>(1796), granej 
  w Warszawie оkоłо 1806 roku lub też litewską pieśń Silvestrasa Valiunasa <em>Dajna 
  Biruta</em> (zaczynającą się słowami "Ant mares kraszta..." а powstałą w XIX 
  wieku), albo nasą pieśń <em>Serdeczna matko.</em> Otóż wszystkie te utwory роwstały 
  dużo później niż hejnał maryjny <em>Bądź pozdrowiona Panienko Maryjo</em>, który 
  w XVIII wieku cieszył się w Polsce dużą popularnością wsród śpiewów religijnych.<br>
  <br>
  Przez 30 lat między powstaniem listopadowym i styczniowym, tekst pieśni <em>Boże, 
  coś Polskę</em> wzbogacał się о nowe zwrotki (m.in. autorstwa Wincentego Smagłowskiego 
  i innych, nieznanych poetów). Około 1860 roku, podczas manifestacji warszawskich 
  oraz w powstaniu styczniowym - pieśń ta liczyła juz jedenascie zwrotek (i ten 
  tekst został tu podany). Ranga pieśni w powstaniu z 1863 roku byłа tak wielka, 
  iż bard tego powstania, Ernest Buława (czyli Władysław hr. Tarnowski, autor 
  powstańczej <em>Pieśni żołnierza</em> znanej dziś powszechnie pod tytułem <em>Jak 
  to na wojence ładnie</em>) określił <em>Boże, coś Polskę</em> mianem hymnu narodowego. 
  <br>
  <br>
  W drugiej połowie XIX i na początku ХХ wieku, pieśń ta była znana i śpiewana 
  w języku angielskim, białoruskim, czeskim, francuskim, litewskim, łotewskim, 
  niemieckim (również inna pieśń niemiecka <em>Wer will die Heitmath</em>, w drugiej 
  częsci oparta jest na melodii <em>Boże, coś Polskę</em>), rosyjskim, serbo-chorwackim, 
  słowackim, ukraińskim; węgierskim i włoskim - jako pieśń religijna, а wśród 
  wymienionych tu narodów słowiańskich również jako pieśń patriotyczna. Była także 
  śpiewana przez Żydów jako Modlitwa Izraelitów na Nowy Rok 5622 (czyli 1861, 
  ze słowami: <em>Boże, coś wieki Izraela naród...)</em>. <br>
  <br>
  Po odzyskaniu niepodległości w 1918, w refrenie zacępto śpiewać "Ojczyznę wolną 
  pobłogosław Panie&quot; i pieśń ta, dzięki swojemu podniosłemu charakterowi 
  hymnicznemu i dzięki patriotycznej tradycji jej wykonywania, stała się jedną 
  z kandydatek do miana hymnu państwowego. Na Śląsku śpiewano ją jako <em><a href="slaskihymn.htm">Śląski 
  hymn narodowy</a></em> (z refrenem "Spod jarzma Niemców wybawże nas Panie&quot;).<br>
  <br>
  W okresie okupacji hitlerowskiej, а potem podczas śpiewów manifestujących stosunek 
  do PRL - w refrenie znów powrócił zwrot "Ojczyznę wolną racz nam wrócic, Panie". 
  W XIX i ХХ wieku <em>Boże, coś Polskę</em> była drugą, obok <em>Mazurka Dąbrowskiego</em>, 
  najważniejszą pieśnią narodową Polaków, pełniącą rolę nie tylko hymnu narodowego 
  ale i religijnego. <br>
