	<br><span class="text">
	<span class="header"><strong>Himno 
  Revolucionario Mexicano<br>
  (Мексиканский революционный гимн)</strong></span>
	<br>
<br>
Texto:  Enrique 
  Flores Mag&oacute;n<br>
  M&uacute;sica: Jaime Nunó
  (la m&uacute;sica del Himno Nacional Mexicano) <br>
  <br>
  (Coro)<br>
<br>
Proletarios: al grito de guerra,<br>

Por ideales luchad con valor;<br>

Y expropiad, atrevidos, la tierra<br>

Que detenta nuestro explotador.<br>

<br>
I. 

Proletarios: precisa que unidos<br>

Derrumbemos la vil construcción<br>

del Sistema Burgués que oprimidos<br>

Nos sujeta con la explotación;<br>

que ya es tiempo que libres seamos<br>

Y dejemos también de sufrir.<br>

Siendo todos iguales y hermanos,<br>

Con el mismo derecho vivir.<br>
<br>

(Coro)<br>
<br>

II. 

Demostremos que somos conscientes,<br>

Y que amamos el ideal de verdad,<br>

Combatiendo tenaces de frente<br>

Al rico, al fraile y a la Autoridad:<br>

Pues si libres queremos, hermanos,<br>

Encontrarnos algún bello día.<br>

Es preciso apretar nuestras manos<br>

En los cuellos de tal trilogía.<br>
<br>

(Coro)<br>
<br>
III. 
Al que sufra en los duros presidios<br>

Por la causa de la humanidad.<br>

Demos pruebas de ser sus amigos<br>

Y luchemos por su libertad

<br>
Que es deber arrancar de las garras

<br>
De los buitres des Dios Capital<br>

A los buenos que, tras de las barras,<br>

Amenaza una pena mortal<br>
<br>

(Coro)<br>
<br>


IV. Si en la lucha emprendida queremos

<br>
Conquistar nuestra emancipación,<br>
Ningún Jefe imponerse dejemos,<br>
E impidamos así una traición<br>
Pues los hombres que adquieren un puesto<br>
En al cual ejercer un poder.<br>
Se transforman tiranos bien presto<br>
Porque el medio los echa e perder.


<br>
<br>
(Coro)

<br>
<br>
V. Proletarios: alzad vuestras frentes,

<br>
Las cadenas de esclavos romped.

<br>
Despojaos de prejuicios las mentes

<br>
Y las Nuevas Ideas aprended.

<br>
Y al llamar del clarín a la guerra,

<br>
Con arrojo al combate marchad

<br>
A tomar para siempre la Tierra

<br>
Y también a ganar Libertad!

<br>
<br>
(Coro)<br>
