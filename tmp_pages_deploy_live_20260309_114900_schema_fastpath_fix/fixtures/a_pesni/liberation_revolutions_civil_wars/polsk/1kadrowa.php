  Kompanio kochana, <br>
  Nie masz to jak pierwsza, nie!<br>
  <br>
  Chociaż do Warszawy <br>
  Mamy długą drogę, <br>
  Аlе przejdziem migiem, <br>
  Вуlе tylko &quot;w nogę&quot;. <br>
  <br>
  Ojda, ojdadana...<br>
  <br>
  Choć Moskal psiawiara <br>
