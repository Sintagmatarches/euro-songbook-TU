  <br>
  <br>
  <img src="1kadrowa.png" width="771" height="239"><br>
  <br>
&quot;Wiadomości Polskie&quot;, 1915, nr 24 z 4 IV, s. 8—9<br>
  <br>
  mel. — Zbyszko W. Mroczek, Muza Legionów Polskich 1914-1915. Oryginalne śpiewy 
  i śpiewki wojenne z melodyami zebrał..., Kraków 1916, s. 15—16<br>
  <br>
  por.: Polskie pieśni wojenne i piosenki obozowe, zebrał Ad(am) Z(agórski), Piotrków 
  1915, s. 55—56 (pt. Pierwsza kadrowa); Wacław Biernacki-&quot;Kostek&quot;, 
  Marsze i piosenki Brygady Piłsudskiego, Kraków 1915, s. 4—5 (pt. Kadrówka); 
  Zbyszko W. Mroczek, Muza Legionów Polskich 1914-1915, Kraków 1916, s. 15—16 
  (pt. Pieśń pierwszej kompanii kadrowej); Stanisław Łempicki, Adam Fischer, Polska 
  pieśń wojenna. Antologia poezyi polskiej z roku wielkiej wojny, wydali..., Lwów 
  1916, s. 186—187 (pt. Raduje się serce...)<br>
  <br>
  W artykule Narodziny Kadrówki zamieszczonym w specjalnym numerze &quot;Muzyki&quot; 
  (1935, nr 5—7) żołnerz Kompanii Kadrowej Wacław Łęcki-&quot;Graba&quot; wspominał 
  okoliczności powstania popularnej piosenki: &quot;Nосą z 5 na 6 sierpnia wyszliśmy, 
  mijając pod parkiem Jordana salutującego w milczeniu Komendanta. Niepewność 
  skończyła się niebawem: u końca parku zamiast pod Kopiec Kościuszki skręciliśmy 
  w prawo — w stronę granicy. Cicho obchodziliśmy Kraków, gdzie mogli nas zatrzymać 
  Austriacy. Gdzieś koło Prądnika zatrzymał nas jadący bryczką nasz 'intendent', 
  obecny gen. Litwinowicz i uraczył bułkami i kiełbasą nasze puste od wczoraj 
  żołądki. Nastrój poważny poweselał, rozłożyliśmy się na trawe, а na środek drogi, 
  zagryzając bułkę, wystąpił Oster i z miejsca zaśpiewał nam piosenkę, która tak 
  oddawała nasz nastrój w tej chwili, że raz podchwycona już nie zeszła z ust 
  kompanii naszej i obiegła rychło pułk cały i całe Legiony... А melodia pieśni? 
  Nie nastarcza ona żadnych problemów. Tekst ulożony został do znanej i śpiewanej 
  w Strzelcu melodii andrusowskiej piosenki znad Wisły: Siwa gąska, siwa, po Dunaju 
  pływa.&quot; <br>
  <br>
  W tym samym artykule Wacław Łęcki-&quot;Graba&quot; przyznał się do autorstwa 
  dwóch zwrotek: szóstej (" А gdy się szczęśliwie zakończy powstanie&quot;) oraz 
  ostatniej (Zbyszko W. Mroczek, Muza Legionów Polskich 1914-1915. Kraków 1916, 
  s. 17):<br>
  <br>
  Może by nas wcale nie bolały nogi, <br>
  Gdyby komendanci nie skracali drogi. <br>
  <br>
  (1) — Pierwsza Kompania Kadrowa — Pierwszy od 1863 г. oddzial WP, sformowany 
  do bezpośredniej walki z zaborcami w wyniku mobilizacji oddziałów Związku Strzeleckiego 
  oraz Polskich Drużyn Strzeleckich, ogłoszonej przez Józefa Piłsudskiego. Oddział 
  tworzony w krakowskich Oleandrach (pawilon powystawowy) 3 VIII 1914 г., wyruszył 
  6 VIII (о godz. 3.30) i о godzinie 9.45 przekroczył w Michałowicach granicę 
  Królestwa z zadaniem wywolania tam powstania antyrosyjskiego. Do dzisiaj nie 
  udało się ustalić dokładnego stanu liczbowego &quot;Kadrówki&quot;. Tadeusz 
  Kasprzycki w swoich wspomnieniach przytoczył listę 144 kadrowiaków (bez patrolu 
  Beliny), natomiast Wacław Sieroszewski mówił о 98 strzelcach i 74 drużyniakach 
  (razem 172 osoby). Kompanią dowodził Tadeusz Kasprzycki, dowodcami plutonów 
  byli: Kazimierz Piatek-&quot;Herwin&quot; (I pluton), Henryk Paszkowski-&quot;Krok&quot; 
  (II), Stanisław Burhardt-&quot;Bukacki&quot; (III) i Jan Kruszewski-&quot;Kruk&quot; 
  (IV). <br>
  <br>
  9 VIII w Książu Wielkim do oddziału dołączył batalion strzelecki dowodzony przez 
  Mieczysława Neugebauera-&quot;Norwida&quot;. Tak powiększony oddział wszedł 
  ostatecznie w skład 1 рр jako III batalion (dowódca Edward Rydz-&quot;Śmigly&quot;). 
  <br>
  <br>
  (2) — mannlicher — karabin powtarzalny, odtylcowy, pięciostrzałowy, konstrukcji 
  austriackiej (Ferdynand Mannlicher 1848—1904), stanowiący podstawowe uzbrojenie 
  pojedynczego żołnierza armii austro-węgerskiej. <br>
  <em><br>
  A gdy na wojenkę szli Ojczyźnie służyć. Pieśni i piosenki żołnierskie 
  z lat 1914-1918. Antologia. Opracował Adam Roliński, Kraków: Uniwersytet Jagielloński, 
  1989.</em><br>
