  Tekst pieśni napisał w nocy z 1 na 2 lutego 1831 roku uczestnik powstania listopadowego, 
  dziennikarz, poeta i geograf Andrzej Słowaczyński. Utwór ten został poświęcony 
  "na dzień odjazdu Naczelnego Wodza ze stolicy do obozu&quot; (dowódca powstania, 
  Józef Chłopicki miał odjechać do obozu wojennego pod Olszynką Grochowską). Muzykę 
  do tego tekstu, na głosy solowe, chór i orkiestrę skomponował dyrektor warszawskiej 
  sceny operowej, Karol Kurpiński. (Jest to jego jedna z ośmiu pieśni powstańczych, 
  jakie w tym czasie stworzył, które były - "ku pokrzepieniu serc&quot; - wykonywane 
  podczas przerw lub po zakończeniu spektakli operowych.) <br>
  <br>
  Marsz obozowy został ро raz pierwszy zaśpiewany w Teatrze Narodowym, podczas 
  przedstawienia operowego dyrygowanego przez Karola Kurpińskiego - 8 lutego 1831 
  roku, z okazji odjazdu generała Chłopickiego z Warszawy. <br>
  <br>
  Pierwsze takty melodii tej pieśni stały się w ХХ wieku hasłem Wojska Polskiego 
  (wykonywanym do dziś przez trębaczy wojskowych). <br>
  <br>
  ***<br>
  <br>
  <strong>Karol KURPIŃSKI</strong> (1785-1837), kompozytor, pedagog, dyrygent, 
  publicysta muzyczny (założyciel pierwszego polskiego pisma muzycznego - "Tygodnik 
  Muzyczny&quot;), dyrektor opery narodowej. Czołowy przedstawiciel polskiego 
  preromantyzmu muzycznego, najwybitniejszy twórca operowy przed Moniuszką, autor 
  wielu pieśni patriotycznych i religijnych. Skomponował m.in. <em><a href="warsz1831.htm">Warszawiankę</a></em> 
  (sł. С. Delavigne, tłum. К. Sienkiewicz) i <em>Marsz obozowy</em> (sł. А. Slowaczyński). 
  <br>
  <br>
  <strong>Andrzej (Jędrzej) SŁOWACZYŃSKI</strong> (1805 - ро 1847), poeta, dziennikarz, 
  geograf, statystyk, uczęstnik powstania listopadowego. Po upadku powstania wyemigrował 
  do Paryża, gdzie wydawał "Tygodnik Emigracji Polskiej&quot; (1833-37); był autorem 
  pierwszego polskiego słownika geograficznego <em>Polska w kształcie dykcjonarza 
  historyczno-statystyczno-geograficznego opisania</em> (1833-38); w 1844 przygotował 
  do wydania <em>Atlas krain polskich</em>. Jest autorem tekstów pieśni powstanczych 
  m.in. <em>Marsza obozowego</em> (muz. К. Kurpińskiego). <br>
