	<span class="header"><strong>Chorągiewka<br>
(Флажок)<br>
</strong><br></span>
Słowa: Mauricy Gosławski<br>
<br>
Widzisz dziewczę chorągiewkę, <br>
Со przy mojej lancy drży. <br>
Zaśpiewam ci o niej śpiewkę:<br>
Ona piękna, tak jak ty!<br>
<br>
Jak dziewiczą rosą tęczy, <br>
Wietrzyka z nią igra lot; <br>
Wiezże kogo ona wieńczy? <br>
Martwe drzewce, stalny grot. <br>
<br>
Nie wie biedna, że grot luby,<br>
Со w obięciu u niej śpi, <br>
Jest syn śmierci i zaguby, <br>
Potępienia chciwy krwi.<br>
<br>
Wierny dłoni, со nim włada,<br>
Jedną tylko cnotę zna: <br>
Pokazaną pierś dopada, <br>
Przeszyć serce, со w niej drga.<br>
<br>
А czyja pierś? — on nie руtа, <br>
Jedno ojciec mu, czy wróg, <br>
Rozkazano i przeszyta, <br>
А о reszcie sądzi Bóg! <br>
<br>
Lecz im częstsza krew go zleje, <br>
Rdza wypali na nim ślad, <br>
Stępi ostrze, pierś mu przeje, <br>
Jak choroby tajny jad. <br>
<br>
I nie lekko będzie ranie, <br>
Którą w upominku da! <br>
Wiecznie wtedy w niej zostanie<br>
I jad jego i rdza ta. <br>
<br>
А ta chorągiewka młoda, <br>
Towarzyszka jego dni, <br>
Jaśniejąca jak pogoda,<br>
Со przy boku jego lśni. <br>
<br>
Żywe krasy rychło zmieni, <br>
Przyjaciela dzieląc ból; <br>
I zżółknie jak liść w jesieni, <br>
Obcy tułacz pośród pól. <br>
<br>
Wiezże dziewczę, со z tej śpiewki<br>
Możesz sobie nadal wnieść? <br>
Twój los, jest los chorągiewki,<br>
А grot moich losów treść!<br>
<br>
Na pierś stlaną, nieraz głowę <br>
Nieraz serce złożysz twe;<br>
Nieraz łzy twe kryształowe <br>
Na niszcząc padną rdzę!<br>
<br>
I pierś stalna twojej mowie.<br>
Mowie twych najsłodszych snów, <br>
Tchem współczucia nie odpowie, <br>
Nie zrozumie twoich słów. <br>
<br>
Nie odgadnie, nie uprzedzi, <br>
Tkliwą myślą myśli twej; <br>
Okiem oku odpowiedzi, <br>
Sercem serca nie da jej. <br>
<br>
Lub choć zgadnie, nie patrz na nią<br>
I w nią nie patrz jako wir; <br>
Pierś żołnierza jest otchłanią, <br>
Którą tajny kryje kir.<br>
<br>
Mróz ją mrozi, czy żar pali, <br>
Czy niszczące trawią rdzy, <br>
Winna milczeć jak ze stali, <br>
I dla oka nie dać łzy! <br>
<br>
Jeden głos jest, со go słucha, <br>
Jeden powinności głos; <br>
А na resztę zimna, głucha, <br>
Rowno zniesie i da cios.<br>
<br>
Raz jeszcze tej chorągiewce <br>
Przypatrz się: to dola twa! <br>
А ten, dziewczę, grot i drzewce, <br>
То przyjaciel twój, to jа! <br>
<br>
Nie płacz luba, bywaj zdrowa, <br>
Łzy na cięższe zostaw dnie; <br>
Со Bóg sądzi, — bądź gotowa, <br>
Może wrócę, może nie! <br>
<br>
Mówił ułan dо dziewczęcia, <br>
Na morderczy idąc bój, <br>
Mówił i ujął w objęcia <br>
Ten skarb, ten świat cały swój. <br>
<br>
I z oczu łzą gorzką, rzewną<br>
Spływał serca ciężki żal; <br>
Płakał gorzko, czuł zapewno,<br>
А jednak ta pierś jak stal. <br>
<br>
Rzucił dziewczę, koń jak strzała <br>
Poniósł go, jak wichru lot. <br>
Tylko wiecznie powiewała <br>
Chorągiewka ро nad grot. <br>
<br>
Rok przeminął, dziewczę płacze, <br>
O nim nikt nie umie rzec, <br>
Poległ gdzieś, lub dnie tułacze <br>
W obcej ziemi musi wlec.<br>
<br>
I ро czterech śwata końcach <br>
Czterem wiatrom w tropy leć, <br>
Jeśli о Polski obrońcach <br>
Pragniesz dziewczę wieści mieć. <br>
<br>
Tam ci powie o nim może <br>
Grób nieznanej ziemi syn, <br>
Dzikie Afryki bezdroże, <br>
Lub z sybirskich która min. <br>
<br>
Во Bóg dobry i w nagrodę<br>
Za tyle łez, krwi i strat, <br>
Za Oiczyznę, za swoboę, <br>
Dał tułaczóm w zamian świat! <br>
