    <br>
    Самая популярная ныне из песен итальянских партизан времен Второй Мировой войны. 
    Вероятнее всего, написана в 1940-е годы неизвестным  автором в моденских 
    Апеннинах. Бытовала в области Эмилия - между болонскими Апеннинами и территорией 
    Партизанской республики Монтефьорино. <br>
	<br>
	Мелодия, по одной версии, заимствована из старой детской 
    песенки о сонном зелье, по другой - из народной песни на идиш &quot;Dus Zekele mit Koilen&quot; (&quot;Маленький мешочек с углем&quot;). В 1919 году эту еврейскую песню записал с аккордеоном на пластинку в Нью-Йорке эмигрант из России Мишка Цыганов (под заглавием &quot;Koilen&quot;, только мотив), в 1921 году (тоже в Америке) - с текстом Авраам Московиц,  а в 1922 году  в Нью-Йорке - снова с текстом Моррис Гольдштейн (аранжировщиком на пластинке указан Рубен Шапиро). Похожий на &quot;Bella ciao&quot; мотив в ней слышится отчетливо, как и мотив другой популярной песни - <a href="../grvojna/kr/krasnarmia.php">&quot;Красная армия всех сильней&quot;</a>.  
	См.  Jenner Meletti. <a href="a-bellameletti.php">Da ballata yiddish a inno partigiano:
il lungo viaggio di Bella ciao</a> (&quot;La Repubblica&quot;, 12.04.2008), или в русском переводе - Дженнер Мелетти. <a href="a-bellameletti-rus.php">Длинный путь "Bella ciao": от песни идиш до партизанского гимна</a>. Возможно, &quot;Koilen&quot; - также прообраз мелодии <a href="../dvor/bubliki.php">"Бублички"</a>.<br>
