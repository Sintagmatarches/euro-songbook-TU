    В тексте &quot;Bella ciao&quot; использованы мотивы итальянских народных 
    песенок, прежде всего &quot;Fior di tomba&quot; (&quot;Цветок на могиле&quot;) 
    и &quot;E picchia picchia la porticella&quot; (&quot;И стучит, стучит дверца&quot;). <br>
    <br>
    Песня выглядит оптимистически, но для итальянских партизан, знавших песни-источники, 
    она таковой вряд ли была - скорее, это песня идущих на смерть и жаждущих ее. 
    Сюжет &quot;Цветка на могиле&quot;: девушка, которую бросает возлюбленный, 
    ищет себе смерти.<br>
    <br>
    Мировую известность Bella ciao приобрела уже после войны, во время 1-го Международного фестиваля молодежи 
    и студентов, проходившего в Праге с 25 июля по 16 августа 1947 года, куда ее привезли сами итальянские 
    партизаны. После этого переведена на многие языки и вошла в репертуар исполнителей 
    из разных стран.<br>
    <br>
    Во время студенческих волнений 1968 года появился еще один куплет о том, что 
    знамя павшего партизана было красным:<br>
    <br>
    <em>Era rossa la sua bandiera <br>
    O bella ciao, bella ciao, bella ciao ciao ciao <br>
    Era rossa la sua bandiera <br>
    c'era scritto libert&agrave;. </em><br>
    <br>
    (Анархистам можно петь о черном флаге: &quot;Era NERA la sua bandiera…&quot;)<br>
    <br>
    Есть несколько вариантов русских текстов, в том числе &quot;пионерский&quot;, 
    который учили в советских школах в 1980-е гг. на уроках музыки - <a href="../baby/mamaciao.php">&quot;Мама, 
    чао!&quot;</a> (текст Александра Тверского). Из советских исполнителей песню первым 
    спел в начале 1960-х Муслим Магомаев по-итальянски и по-русски с текстом Анатолия 
    Горохова. 
    В исполнении Магомаева вошла в советский фильм &quot;По следу Тигра&quot;. Песня 
    использована также в фильме Петра Тодоровского &quot;Какая чудная игра&quot; (время 
    действия - послевоенные годы) - на протяжении всего фильма ее поют и слушают 
    на пластинке на русском языке студенты в общежитии. <br>
    <br>
    В 1999 году Гарик Сукачев использовал ее мотивы в <a href="../rock/sukatchev/pvolnstrelka.htm">&quot;Песне 
    вольного стрелка (Белла Чао!)&quot;</a>, причем для припева взял оригинальную 
    мелодию песни &quot;Bella ciao!&quot;<br>
