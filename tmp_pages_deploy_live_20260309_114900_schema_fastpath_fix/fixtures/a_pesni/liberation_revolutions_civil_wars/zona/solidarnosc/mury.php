	<br>
        Słowa: Jacek Kaczmarski<br>
        <br>
        On natchniony i młody był, ich nie policzyłby nikt<br>
          On im dodawał pieśnią sił, śpiewał że blisko już świt.<br>
          Świec tysiące palili mu, znad głów podnosił się dym,<br>
          Śpiewał, że czas by runął mur...<br>
          Oni śpiewali wraz z nim:<br>
	<br>Wyrwij murom zęby krat!<br>
          Zerwij kajdany, połam bat!<br>
          A mury runą, runą, runą<br>
          I pogrzebią stary świat!<br>
	<br>Wkrótce na pamięć znali pieśń i sama melodia bez słów<br>
          Niosła ze sobą starą treść, dreszcze na wskroś serc i głów.<br>
          Śpiewali więc, klaskali w rytm, jak wystrzał poklask ich brzmiał,<br>
          I ciążył łańcuch, zwlekał świt...<br>
          On wciąż śpiewał i grał:<br>
	<br>Wyrwij murom zęby krat!<br>
          Zerwij kajdany, połam bat!<br>
          A mury runą, runą, runą<br>
          I pogrzebią stary świat!<br>
	<br>Aż zobaczyli ilu ich, poczuli siłę i czas,<br>
          I z pieśnią, że już blisko świt szli ulicami miast;<br>
          Zwalali pomniki i rwali bruk - Ten z nami! Ten przeciw nam!<br>
          Kto sam ten nasz najgorszy wróg!<br>
          A śpiewak także był sam.<br>
	<br>Patrzył na równy tłumów marsz,<br>
          Milczał wsłuchany w kroków huk,<br>
          A mury rosły, rosły, rosły<br>
          Łańcuch kołysał się u nóg...<br>
	<br>Patrzy na równy tłumów marsz,<br>
          Milczy wsłuchany w kroków huk,<br>
          A mury rosną, rosną, rosną<br>
          Łańcuch kołysze się u nóg...<br>
          <br>
          1978 
        <br>
		<br>
        <a href="http://www.kaczmarski.art.pl" target="_blank"><em>http://www.kaczmarski.art.pl</em></a><br>
        <br>
        <br>
        Одна из самых известных песен Яцека Качмарского, неофициальный гимн польской 
        «Солидарности». На музыку песни каталонского певца Люиса Ляка (Lluis Llach) 
        «L’estaca», написанной в 1968 г. и ставшей символом борьбы против диктатуры 
        Франко. Есть несколько русских переводов. Песней стал <a href="../../novrev/steny.php">перевод Кирилла Медведева</a> (группа &quot;Аркадий Коц&quot;), сделанный в начале 2012 г., в ходе белоленточных протестов.<br>
		<br>
		<br>
        <strong>Перевод Андрея Базилевского<br>
        <br>
        Стены </strong><br>
        <br>
        Он пел вдохновенно, он молод был, <br>
        А их там было — не счесть. <br>
        От песни у них прибывало сил, <br>
        Он пел, что свет где-то есть. <br>
        Они тысячи свеч зажгли ему, <br>
        Над их головами — дым. <br>
        Он пел: айда, разрушим тюрьму... <br>
        Они пели вместе с ним. <br>
        <br>
        Зубы решёток вырви у стен! <br>
        Клетки ломай, оковы рви! <br>
        Пусть стены рухнут, рухнут, рухнут, <br>
        Похоронят старый мир! <br>
        <br>
        Скоро ту песню каждый знал, <br>
        Мелодия, даже без слов, <br>
        Была им как свет, была как дурман <br>
        Для их сердец и голов. <br>
        И пели они, и хлопали в такт, <br>
        Хор канонадой звучал, <br>
        Оковы давили, сгущался мрак... <br>
        А он всё пел да играл: <br>
        <br>
        Зубы решёток вырви у стен! <br>
        Клетки ломай, оковы рви! <br>
        Пусть стены рухнут, рухнут, рухнут, <br>
        Похоронят старый мир! <br>
        <br>
        Но вот они поняли, сколько их там, <br>
        Почуяли силу: пора! <br>
        И с песней двинулись по городам: <br>
        К свету — вперёд — ура! <br>
        Кумиров в прах, булыжник в кулак — <br>
        Ты за нас или нет, гражданин? <br>
        Кто одинок — тот наш злейший враг! <br>
        А певец был всё так же один. <br>
        <br>
        Он видел ровный марш толпы, <br>
        Молча прислушиваясь к шагам. <br>
        А стены росли, росли, росли, <br>
        Цепи гремели на ногах... <br>
        <br>
        Он видит ровный марш толпы, <br>
        Молча прислушиваясь к шагам. <br>
        А стены растут, растут, растут, <br>
        Цепи грохочут на ногах... <br>
        <em><br>
        <a href="http://novpol.ru/index.php?id=226" target="_blank">«Новая Польша», 2004, №5</a></em><br>
        <br>
      <br>
        <strong>Перевод Ирины Поляковой<br>
        <br>
        Стены</strong><br>
		<br>
        Он вдохновенным и юным был,<br>
        Им в толпе – счету нет.<br>
        Пел он – всяк силы находил<br>
        В песне про близкий рассвет.<br>
        Свечки они в честь него зажгли,<br>
        Над ними дым проплывал<br>
        Пел он - прочь тюрьмы с лица земли,<br>
        Каждый ему подпевал.
       <br>
        <br>Рви из стен решетки, брат,<br>
          Сбрось кандалы, прочь рабства ад<br>
          Пусть стены тюрем рухнут, рухнут,<br>
          Старый погребут уклад.<br>
        <br>
          Вскоре ту песню знал любой,<br>
          И даже мотив ее сам<br>
          Старые мысли нес с собой,<br>
          Милые этим сердцам.<br>
          Пели и «бис» кричали вслед,<br>
          Как выстрел, хор их гремел.<br>
          Давила цепь, не спешил рассвет<br>
          А он все пел им да пел.<br>
        <br>
          Поняли — хватит их вполне<br>
          Для битвы и для побед.<br>
          Двинулись строем по стране<br>
          С песней про близкий рассвет.<br>
          Кумиры в прах, и дороги — в прах,<br>
          Ты с нами ль в нашей борьбе?<br>
          Кто сам по себе — тот наш злейший враг.<br>
          А он был сам по себе...<br>
        <br>Он видел строй за рядом ряд<br>
          Он слышал их чеканный шаг<br>
          А стены грозно, грозно, грозно<br>
          Росли и цепь звенела в такт.<br>
          <br>
          Он слышит их чеканный шаг,<br>
          Он видит строй за рядом ряд<br>
          А стены грозно, грозно, грозно<br>
          Растут и кандалы гремят.<br>
