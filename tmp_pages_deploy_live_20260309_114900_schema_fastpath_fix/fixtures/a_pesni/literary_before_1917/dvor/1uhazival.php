Раз ухаживал за девушкой три года.<br>
Она дочь капитана была.<br>
И на всё-то она соглашалась,<br>
Потому что любила меня.<br>
<br>
Как-то выпил с друзьями по привычке<br>
И пошёл с ней по улицам гулять.<br>
Долго-долго по улицам гуляли,<br>
А потом пригласил её в сад.<br>
<br>
А в садочке было тихо и спокойно,<br>
Сквозь деревья светила луна.<br>
На зелёном ковре мы сидели,<br>
Целовала Наташа меня.<br>
<br>
У Ванюши тут и кровь разыгралась.<br>
Стал Ванюша к Наташе приставать.<br>
А Наташа ему отвечала:<br>
- Ваня, Ваня, не трогай меня!<br>
<br>
Ты привык с посторонними знаться,<br>
Ты привык посторонних любить.<br>
Надо мною ты решил посмеяться,<br>
Ай, молодую мне жизнь загубить!<br>
<br>
- Не подумай плохого, дорогая!<br>
Не пришёл я тебя звать под венец,<br>
А пришёл я сказать: «До свиданья!»<br>
Значит, нашей любви – конец!<br>
