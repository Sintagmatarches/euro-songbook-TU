<br>
Катюха, нежное созданье, <br>
К тебе ходил я на свиданья. <br>
Раз горит в окошке свет, <br>
Значит, мужа дома нет – <br>
Сейчас, сейчас, сейчас. <br>
<br>
Долго мы с Катюхой обнимались, <br>
Долго зажимались, целовались. <br>
А потом с ней на кровать — <br>
И давай роман читать. <br>
Читал, читал, читал, не дочитал. <br>
<br>
Однажды страшный случай приключился: <br>
Муж с поездки рано возвратился. <br>
Вдруг он дернул, как злодей, <br>
Колокольчик у дверей — <br>
Дзинь, дзинь, дзинь, дзинь.<br>
<br>
Катюха очень испугалась, <br>
И к нему, к дверям помчалась. <br>
А я, как бедный Дон-Жуан, <br>
Из постельки под диван — <br>
Прыг, прыг, прыг, прыг. <br>
<br>
Он меня, как Дон-Жуана, <br>
Стал волокти из-под дивана. <br>
Развернул он свой кулак <br>
И меня по уху так - <br>
Тресь, тресь, тресь, тресь.<br>
<br>
Не помню, что там дальше было... <br>
Катюха двери мне отворила. <br>
По мостовой котенок мчался, <br>
Мне казалось, кто-то гнался – <br>
Ой! Ой! Ой! Ой! <br>
<br>
Теперь, ребята, я вам клянуся, <br>
К Катюхе больше я не вернуся. <br>
А как вспомню мужа взор, <br>
Так в ушах до этих пор: <br>
Тресь, тресь, тресь, тресь! <br>
<br>
        <em>Песни нашего двора / Авт.-сост. Н. В. Белов. Минск: Современный литератор, 
        2003.  (Золотая коллекция). </em><br>
 <br>
<br>
В вариантах песни упоминается городовой - значит, она восходит еще к дореволюционной 
эпохе. <br>
<br>
<br>
<strong>ВАРИАНТЫ (5)<br>
<br>
1. Катюха</strong><br>
<br>
Катюха, нежное созданье, <br>
К тебе ходил я на свиданья... <br>
Раз горит в окошке свет, <br>
Значит, мужа дома нет - <br>
Щас, щас, щас, щас, щас. <br>
<br>
Долго мы с Катюхой обнимались, <br>
Долго целовались, обжимались, <br>
А потом с ней на кровать <br>
И давай роман читать. <br>
Читал, читал, читал - не дочитал. <br>
<br>
Однажды страшный случай приключился: <br>
Муж с поездки рано возвратился. <br>
Вдруг он дернул, как злодей, <br>
Колокольчик у дверей - <br>
Дзинь, дзинь, дзинь, дзинь, дзинь. <br>
<br>
Катюха очень испугалась <br>
И к нему, к дверям помчалась. <br>
А я, как бедный Дон-Жуан, <br>
Из постели под диван - <br>
Прыг, прыг, прыг, прыг, прыг. <br>
<br>
Он меня, как Дон-Жуана, <br>
Стал волокти из-под дивана. <br>
Развернул он свой кулак <br>
И меня по уху так - <br>
Тресь, тресь, тресь, тресь, тресь. <br>
<br>
Не помню, что там дальше было... <br>
Катюха двери мне отворила. <br>
По мостовой котенок мчался, <br>
Мне казалось, кто-то гнался - <br>
Ой! Ой! Ой! Ой! Ой! <br>
<br>
Теперь, ребята, я вам клянуся: <br>
К Катюхе больше я не вернуся. <br>
А как вспомню мужа взор, <br>
Так в ушах до этих пор - <br>
Тресь, тресь, тресь, тресь, тресь. <br>
        <em><br>
        А я не уберу чемоданчик! Песни студенческие, школьные, дворовые / Сост. 
        Марина Баранова.  М.: Эксмо, 2006.</em><br>
<br>
<br>
<strong>2. Катюша</strong> <br>
<br>
Катюша, мое милое созданье,<br>
К тебе опять спешу я на свиданье,<br>
Если в окнах горит свет –<br>
Значит дома мужа нет.<br>
Чап-чап-чап-чап-чап. <br>
<br>
С Катюшей на крыльце мы повстречались,<br>
Обнимались, крепко целовались,<br>
А потом уж на диван, и какой там был роман…<br>
Ой-ёй-ёй-ёй-ёй. <br>
<br>
Вдруг в двери тихо кто-то постучал,<br>
Катя побежала открывать,<br>
Ну а я, как Дон Жуан,<br>
Быстро смылся под диван.<br>
Чап-чап-чап-чап-чап. <br>
<br>
И меня тотчас, как Дон Жуана,<br>
Катин муж извлек из-под дивана.<br>
Развернул он свой кулак, <br>
И поставил мне синяк.<br>
Ой-ёй-ёй-ёй-ёй. <br>
<br>
А дальше я не помню, что и было,<br>
Катюша молча двери отворила,<br>
По мосткам тесовым мчался,<br>
Все казалось - кто-то гнался.<br>
Чап-чап-чап-чап-чап. <br>
<br>
Прибегаю я к себе домой,<br>
У двери встречает городовой,<br>
Говорит он – что и как,<br>
Где же брюки и пиджак.<br>
Ой-ёй-ёй-ёй-ёй. <br>
<br>
Говорю: «Какое ваше дело?<br>
Мне ходить в костюме надоело».<br>
Снял я брюки и пиджак<br>
И решил ходить вот так.<br>
Чап-чап-чап-чап-чап. <br>
<br>
        <em>В нашу гавань заходили корабли. Вып. 5. М.: Стрекоза, 2001.</em><br>
<br>
<br>
<strong>3. Дамочка великого страдания<br>
</strong><br>
Дамочка великого страдания,<br>
Как к тебе спешу я на свиданье,<br>
А в окошке горит свет –<br>
Значит дома мужа нет.<br>
Топ, топ, топ. <br>
<br>
Мы сидели, пили чай,<br>
Муж вернулся невзначай<br>
И давай меня ласкать,<br>
По всем комнатам таскать,<br>
И таскал меня, мой друг,<br>
И на север, и на юг,<br>
Напоследок угостил –<br>
Меня с лестницы спустил.<br>
А ступенек сорок пять,<br>
И все пришлось пересчитать. <br>
<br>
После этих похождений<br>
Я даю совет тебе:<br>
Не ходи к замужней бабе,<br>
Приглашай всегда к себе. <br>
<br>
        <em>В нашу гавань заходили корабли. Вып. 5. М.: Стрекоза, 2001.</em><br>
 <br>
<br>
<strong>4. Наташка </strong><br>
<br>
Наташка, мое чудное созданье,<br>
Давно я не хожу к ней на свиданье,<br>
Хоть и в окнах горит свет,<br>
Хоть и мужа дома нет – нет, нет, нет. <br>
<br>
Со мною был случай такой:<br>
Раньше муж ее пришел домой,<br>
Только мы легли в постель,<br>
Колокольчик у дверей – дзынь, дзынь, дзынь. <br>
<br>
Наташка мужу двери отворила,<br>
Сама куда-то скромно отвалила,<br>
Ну а я, как Дон Жуан,<br>
Моментально под диван – топ, топ, топ. <br>
<br>
Наташкин муж меня, как Дон Жуана,<br>
Извлек тут из-под подлого дивана,<br>
Развернул он свой кулак, и<br>
Задвинул мне он так – ой, ой, ой. <br>
<br>
Наташка мне двери отворила,<br>
На улицу тихонько проводила,<br>
Я по улице помчался,<br>
Мне казалось, кто-то гнался – стой, стой, стой. <br>
<br>
А наутро я пришел домой,<br>
Дома не пускает домовой,<br>
Я ему и так, и сяк…<br>
А он: «Где брюки? Где пиджак?» - твой, твой, твой. <br>
<br>
Наташка, мое чудное созданье,<br>
Давно я не хожу к ней на свиданье,<br>
Хоть и в окнах горит свет,<br>
Хоть и мужа дома нет – нет, нет, нет. <br>
        <em><br>
        В нашу гавань заходили корабли. Вып. 5. М.: Стрекоза, 2001. </em><br>
<br>
<br>
<strong>5.</strong><br>
        <a href="tchap.png" target="_blank"><img src="tchap.png" width="766" height="566" border="0"></a><br>
<br>
<br>
Валюха — мое милое созданье,<br>
Опять к тебе спешу я на свиданье.<br>
Если в окнах горит свет, значит, мужа дома нет,<br>
Чап-чап-чап-чап-чап-чап.<br>
<br>
Валюха как-то мужа провожала,<br>
Рыдала и стонала у причала.<br>
Но лишь отошел баркас, Валя бросилася в пляс,<br>
Чап-чап-чап-чап-чап-чап.<br>
<br>
Мы часто проводили дни и ночи,<br>
Нам весело и сытно было очень:<br>
Муж приедет, денег даст, и опять на свой баркас,<br>
Чап-чап-чап-чап-чап-чап.<br>
<br>
Счастливые часов не наблюдают,<br>
И обо всем на свете забывают.<br>
Вот, однажды, скажем так, мы попали с ней впросак,<br>
Чап-чап-чап-чап-чап-чап.<br>
<br>
Ворвался муж ее домой нежданно,<br>
И голыми он нас застукал в ванной.<br>
Мне он голову разбил, а ее чуть не прибил,<br>
Чап-чап-чап-чап-чап-чап.<br>
<br>
Лечился я мучительно и долго,<br>
И денежек ухлопал очень много.<br>
Я на женщин не глядел, дома целый день сидел,<br>
Чап-чап-чап-чап-чап-чап.<br>
<br>
Теперь я больше не хожу к Валюшке,<br>
Нашел себе смазливую девчушку.<br>
Не боюсь я никого, нету мужа у нее,<br>
Чап-чап-чап-чап-чап-чап.<br>
