	<span class="header"><strong>ЗАВЕЩАНИЕ 
  ПЬЯНИЦЫ</strong><br></span>
  <br>
  Под вечер, осенью ненастной<br>
  Шел горький пьяница домой,<br>
  С разбитым глазом, с носом красным,<br>
  Держал бутылку под полой.<br>
  <br>
  Оборотясь к стене ногами,<br>
  А головой - под самый кран,<br>
  Держал обеими руками<br>
  Большой наполненный стакан.<br>
  <br>
  - Халат и туфли - все продайте,<br>
  Купите пьянице вина,<br>
  Попа до смерти накачайте -<br>
  Вот вам духовная моя.<br>
  <br>
  На крышке гроба напишите<br>
  Какого года, сколько жил,<br>
  Три славных слова подчеркните:<br>
  "Покойник водочку любил".<br>
  <br>
  В колокола вы не звоните<br>
  При погребении моем,<br>
  Почаще рюмками стучите -<br>
  Вот это самый лучший звон!<br>
  <br>
  Друзья, исполните все это,<br>
  Я вас по-царски награжу<br>
  И с того света, с того света<br>
  Бочонок с водкою спущу.<br>
  <br>
  И вот в день Страшного суда<br>
  Картина будет превосходна:<br>
  Пойдут все праведники в рай,<br>
  А пьяницы - в кабак повзводно. <br>
  <br>
        <em>Владимир Бахтин. Русское застолье // "Нева". 1995. №7. С. 231-232.</em><br>
 <br>
  <br>
        Песенка была популярна на лубочных картинках XIX в. Фрагмент ее звучит 
        также в народной героико-романтической драме <a href="../teatr/rusnarod/ermak.htm">&quot;Ермак&quot;</a>. 
        Ср. зачин этой версии с популярным романсом <a href="../popular20/podvetcher.php">&quot;Под 
        вечер, осенью ненастной...&quot;</a> (слова Александра Пушкина, 1814, 
        музыка разных композиторов, в том числе Николая Сергеевича Титова, 1829):<br>
  <br>
  <em>Под вечер, осенью ненастной,<br>
  В пустынных дева шла местах<br>
  И тайный плод любви несчастной<br>
  Держала в трепетных руках.</em><br>
  <br>
  <br>
  <strong>ВАРИАНТЫ (4)<br>
  <br>
  1. Друзья, прощайте, помираю</strong><br>
  <br>
  Друзья, прощайте, помираю,<br>
  Ох, не я, а люди говорят.<br>
  Пальто да брюки оставляю,<br>
  Да две рубахи без заплат;<br>
  <br>
  Телячий ранец и корзину,<br>
  И хуже сапоги,<br>
  И еще одну косынку,<br>
  Да кой-какие пустяки.<br>
  <br>
  Именье это завещаю<br>
  Продать на рынке и потом<br>
  Зайти в трактир, напиться чаю<br>
  И посетить питейный дом.<br>
  <br>
  И там отдайте долг за водку<br>
  Ваньке-скряге-бурсаку,<br>
  Пятак Маланье за селедку<br>
  И грош Борису-кваснику.<br>
  <br>
  В том кабаке меня заройте,<br>
  В котором часто я пивал,<br>
  И так могилушку устройте,<br>
  Чтоб я под бочкою лежал,<br>
  <br>
  Оборотясь к сеням ногами<br>
  И головой под самый кран,<br>
  Держа обеими руками<br>
  Огромный с водкою стакан.<br>
  <br>
  Мою могилу обложите<br>
  Турецким чистым табаком,<br>
  А на могилу положите<br>
  Трубку с длинным чубуком.<br>
  <br>
  Слепите памятник из глины,<br>
  Наймите Ваньку-маляра,<br>
  И он напишет вам картину –<br>
  «Мои великие дела». <br>
<br>
        <em>В нашу гавань заходили корабли. Вып. 3. М.: Стрекоза, 2000.</em><br>
 <br>
  <br>
  <strong>2. Песня алкоголика</strong><br>
  <br>
  Друзья, подагрой изнуренный,<br>
  Не в силах я уж больше пить<br>
  И в этой жизни вожделённой<br>
  Осталось мне недолго жить.<br>
  <br>
  Моя духовная готова,<br>
  Сегодня да завтра да я умру.<br>
  Прошу исполнить слово в слово<br>
  Все, что щас я вам скажу.<br>
  <br>
  В том кабаке меня заройте,<br>
  В котором чаще пировал,<br>
  И мне могилу так устройте,<br>
  Чтоб я под бочкою лежал.<br>
  <br>
  Оборотясь к стене ногами,<br>
  А головой под самый кран,<br>
  И при себе держал руками<br>
  Огромный с водкою стакан.<br>
  <br>
  В колокола вы не звоните <br>
  При погребении моем,<br>
  Лишь часто в бочку застучите,<br>
  Залейте пьяницу вином.<br>
  <br>
  Надгробный стих мне не пишите,<br>
  В каком я чине сколько жил.<br>
  Всего три слова изложите:<br>
  «Покойный водочку любил».<br>
  <br>
  Мундир и брюки вы продайте,<br>
  Купите пьяницам вина<br>
  И всех их пьяных напоите». -<br>
  Вот вам духовная моя!<br>
  <br>
        <em>С фонограммы Теодора Ефимова, CD «В нашу гавань заходили корабли» 
        № 5, «Восток», 2001.</em><br>
 <br>
  <br>
  <strong>3. Завещание пьяницы</strong><br>
  <br>
  Друзья, подагрой изнурённый,<br>
  Уж я не в силах больше пить...<br>
  И , может быть в сей жизни бренной,<br>
  Осталось мне не долго жить.<br>
  <br>
  В том кабаке меня заройте,<br>
  В котором чаще пировал,<br>
  И мне могилу так устройте,<br>
  Чтоб я под бочкою лежал.<br>
  <br>
  Оборотясь к стене ногами,<br>
  А головой под самый кран,<br>
  Держа обеими руками -<br>
  Огромный с водкою стакан.<br>
  <br>
  Надгробных слов не говорите,<br>
        Я не привык к таким словам!<br>
        Цветов вы мне не приносите:<br>
        Не поклонялся я цветам!<br>
  <br>
        Вы эпитафий не пишите,<br>
  Когда родился, сколько жил...<br>
  Тремя словами помяните:<br>
        &quot;Покойный водочку любил!&quot;<br>
  <br>
  В колокола вы не звоните<br>
  При погребении моём -<br>
  А только рюмками стучите, -<br>
  Ведь это самый лучший звон.<br>
  <br>
  Могилу всю мою усыпьте<br>
  Турецким крепким табаком<br>
  Три дня могилу поливайте<br>
  Шампанским искристым вином<br>
  <br>
  Сюртук и брюки вы продайте,<br>
  Купите пьяницам вина!<br>
        Попов до смерти накачайте,<br>
  Вот вся духовная моя!<br>
  <br>
  Когда исполните всё это,<br>
  Тогда я вас озолочу,<br>
  И на верёвке с того света,<br>
  Бочонок с водкою спущу! <br>
        <em><br>
        Прислал Дмитрий Трофимов, ольдерман студенческой корпорации <a href="http://www.arctica.lv" target="_blank">Fraternitas 
        Arctica</a>, 28 июня 2010 г.</em><br>
  <br>
  <br>
  <strong>4. Завещание пьяницы</strong><br>
<br>
Друзья, прощайте, умираю<br>
Не я, а люди говорят,<br>
пальто и брюки оставляю<br>
И две рубашки без заплат.<br>
<br>
Именье это завещаю<br>
Продать на рынке, и потом<br>
Зайти в трактир, напиться чаю,<br>
И посетить питейный дом.<br>
<br>
Телячий ранец и корзинку,<br>
И худые сапоги,<br>
Одну истертую косынку,<br>
И кой-какие пустяки.<br>
<br>
И там отдайте долг за водку<br>
Ваньке скряге бурсаку,<br>
Пятак Маланье и селедку<br>
И грош Борису кваснику.<br>
<br>
Долги все эти уплатите,<br>
По завещании моем.<br>
И в рюмки звонко постучите<br>
Поставьте все их к верху дном.<br>
<br>
В том кабаке меня заройте,<br>
В котором чаще я пивал<br>
И так могилу мне устройте,<br>
Чтоб я под бочкою лежал.<br>
<br>
Оборотясь к стене ногами,<br>
А головой под самый кран,<br>
Держа обоими руками<br>
Огромный с водкою стакан.<br>
<br>
В колокола вы не звоните<br>
При погребении моем,<br>
Но чаще рюмками стучите<br>
Залейте пьяницу вином.<br>
<br>
Надгробной вы мне не пишите,<br>
Какого чина, сколько жил,<br>
А лишь три слова напишите:<br>
«Покойный водочку любил».<br>
<br>
Вы могилу обложите<br>
Турецким чистым табаком, <br>
А на могилу положите<br>
Мне трубку с длинным чубуком.<br>
<br>
Слепите памятник из глины,<br>
Наймите Ваньку маляра,<br>
И он напишет вам картину<br>
Мои великие дела.<br>
