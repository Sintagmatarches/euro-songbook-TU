	<span class="header"><strong>МАРУСЯ 
  ОТРАВИЛАСЬ</strong> <br></span>
  <br>
  Жизнь так несложна, крайне так проста:<br>
  У ворот карета «Красного Креста».<br>
  Может быть, хотите знать, что там случилось?<br>
  Коротко и ясно: Маруся отравилась.<br>
  <br>
  В мастерской бедняжка на машинке шила<br>
  И про свое детство вспоминать любила,<br>
  Как в далеком поле васильки сбирала<br>
  Из душистых лилий как венки сплетала.<br>
  <br>
  Смолоду Маруся на фабрику попала<br>
  И, как птичка, таять понемногу стала.<br>
  В Алешку-гармониста, бедная, влюбилась,<br>
  За него страдала, на него молилась.<br>
  <br>
  А носил Алешка кудри золотые,<br>
  Знал великолепно песни городские.<br>
  Как Марусе бедной было не влюбиться,<br>
  Ну как же при измене ей не отравиться!<br>
  <br>
  Разлюбил Алешка бедную Марусю<br>
  И завел другую – новенькую Дусю.<br>
  А ее, бедняжку, с фабрики прогнали<br>
  И потом на Невском часто так встречали.<br>
  <br>
  Вот вам и картина дня нашей столицы…<br>
  Сверху показались испуганные лица.<br>
  А Маруся сразу резко изменилась.<br>
  Нет уже Маруси, Маруся отравилась.<br>
  <br>
        <em>В нашу гавань заходили корабли. Пермь, &quot;Книга&quot;, 1996. Перепечатано: 
        В нашу гавань заходили корабли. Вып. 2. М., Стрекоза, 2000. </em><br>
<br>
  <br>
  Входила в репертуар Юрия Морфесси (1882-1957). По данным издания <em>Очи черные: 
        Старинный русский романс. М.: Изд-во Эксмо, 2004, </em>запись на пластинку сделана фирмой &quot;Зонофон&quot; в 1913 году в Петербурге, номер пластинки 3-62119, а автор песни - Н. Ларин. Однако на обложке этой пластинки, опубликованной на сайте <a href="http://russian-records.com/details.php?image_id=21089"  target="_blank">Russian-records.com</a> (ее полный номер Х-3-62119), значится &quot;нар. песня&quot;, а сама пластинка датирована 1912 годом. Там же на сайте можно прослушать фонограмму.<br>
  <br>
  Гораздо более известна другая песня с таким же названием, сочиненная пианистом-аранжировщиком 
  ресторана &quot;Яр&quot; Яковом Пригожим и записанная в 1911 году на пластинку 
  Ниной Дулькевич (под заглавием &quot;Маруся умерла&quot;) - см. <a href="marusja.php">&quot;Маруся 
  отравилась&quot; (Как солнце закатилось...)</a>. И, наконец, см. еще одну песенку 
  - про безуспешные попытки вылечить Марусю и ее отправку на кладбище - <a href="marusja-vetch.php">&quot;Маруся&quot;</a> 
  (Вечер вечереет...), и пародию на нее конца 1920-х гг. <a href="16nozej.php">&quot;Шестнадцать 
  столовых ножей&quot;</a> (Жила-была Маруся...).<br>
  <br>
        Подробнее о цикле песен про Марусю см.: Сергей Неклюдов <a href="moskva/a-marusia.php">&quot;Почему 
        отравилась Маруся?&quot;</a> (2008). Пластинка Морфесси в статье датируется 1913 годом со ссылкой на данные Российского государственного архива фонодокументов.<br>
