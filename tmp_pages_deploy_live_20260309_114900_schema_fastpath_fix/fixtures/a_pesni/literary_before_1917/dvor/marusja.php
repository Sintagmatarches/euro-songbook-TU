        <h1>МАРУСЯ ОТРАВИЛАСЬ</h1> 
<span class='i'>Бытовой романс</span><br>
  <br>
  Как солнце закатилось,<br>
  Умолк шум городской,<br>
  Маруся отравилась,<br>
  Вернувшися домой.<br>
  <br>
  В каморке полутемной,<br>
  Ах, кто бы ожидал,<br>
  Цветочек этот скромный<br>
  Жизнь грустно покидал.<br>
  <br>
  Измена, буря злая,<br>
  Яд в сердце ей влила.<br>
  Душа ее младая<br>
  Обиды не снесла.<br>
  <br>
  Ее в больницу живо<br>
  Решили отвезти,<br>
  Врачи там терпеливо<br>
  Старалися спасти.<br>
  <br>
  - К чему старанья эти!<br>
  Ведь жизнь меня страшит,<br>
  Я лишняя на свете,<br>
  Пусть смерть свое свершит.<br>
  <br>
  И полный скорби муки<br>
  Взор к небу подняла,<br>
  Скрестив худые руки,<br>
  Маруся умерла.<br>
  <br>
        <span class='i'>Очи черные: Старинный русский романс. М.: Эксмо, 2004. - 
        в разделе: &quot;Из репертуара Саши Давыдова (1849-1911)&quot;, без подписи 
        автора. Маловероятно, что это мог петь Давыдов, так как он умер в год 
        рождения песни (1911), причем в последние годы жизни не пел.</span><br>
<br>
  <br>
        Шлягер 1911 года. Песня вышла в том году на пластинке Нины Дулькевич, 
        с авторством Якова Пригожего, пианиста и аранжировщика московского ресторана 
        &quot;Яр&quot; (фирма &quot;Сирена Рекорд&quot;, Петербург, 1911, под 
        загл. &quot;Маруся умерла&quot;, подпись: Я. Пригожий. См.: Очи черные: 
        Старинный русский романс. М.: Эксмо. 2004. С. 175). Мотив 
        заимствован из песни <a href="../popular20/razlukaty.htm">&quot;Разлука 
        ты, разлука&quot;</a>. <br>
  <br>
  Исследователь истории грамзаписи Глеб Скороходов 
  пишет, что в том 1911 г. &quot;Марусю&quot; записали на пластинки сразу несколько 
  фирм, уже через год песня потеряла авторство и на пластиночных этикетках стала 
  указываться как народная (Скороходов Г.А. Тайны граммофона: Все неизвестное 
  о пластинках и звездах грамзаписи. М.: Эксмо; Алгоритм, 2004. С. 262). <br>
  <br>
  Песня сохраняла популярность на протяжении десятилетий. В клоунаде Виталия Лазаренко  &quot;Шарманка&quot; (1919) звучат &quot;Маруся отравилась&quot; и <a href="../army/poslnyn.php">&quot;Последний нонешний денечек&quot;</a>.<br>
  <br>
  <br>
  Из книги Сергея Макарова &quot;Клоунада мирового цирка: История и репертуар&quot;, 
  М.: РОССПЭН, 2001. С. 69:<br>
  <em><br>
  Нередко, кроме своего основного выступления, Виталий Лазаренко выходил к зрителям 
  еще раз, исполняя, как он сам называл, &quot;злободневный номер&quot;. В 1919 
  году в программе цирка Никитиных он вторично появился во втором отделении программы 
  с большой сценкой &quot;Шарманка&quot;. Приведем выдержки из текста:<br>
  <br>
  Мне прыгать что-то надоело<br>
  И, граждане, признаюсь вам,<br>
  Я за другое взялся дело:<br>
  Хожу с шарманкой по дворам.<br>
  Шарманки звуки то тоскливы,<br>
  А то бравурны иногда.<br>
  Вам всем знакомые мотивы<br>
  Она играет хоть куда,<br>
  <br>
  - шарманщик поет песню &quot;Маруся отравилась&quot;.<br>
  <br>
  ***<br>
  <br>
  В трамвай я втиснулся насильно.<br>
  Там сжали так со всех сторон,<br>
  Что пот с меня потек обильно,<br>
  Я был в лепешку превращен.<br>
  А уж потом я убедился,<br>
  В том, что свалял я дурака.<br>
  Я золотых часов лишился,<br>
  Не досчитался кошелька.<br>
  <br>
  - шарманщик исполняет песню &quot;Последний нонешний денечек&quot;.<br>
  <br>
  ***<br>
  <br>
  Куда-то спрятались дровишки,<br>
  И десять тысяч стоит воз.<br>
  Синеют малые детишки.<br>
  В квартирах ветер и мороз.<br>
  Сидят и щелкают зубами<br>
  Во всех квартирах москвичи.<br>
  Обзавестись нельзя дровами,<br>
  Хоть караул теперь кричи!*<br>
  <br>
  *ЦГАЛИ. Ф. 2087. Оп. 1. Ед. хр. 80. Л. 143-145.</em><br>
