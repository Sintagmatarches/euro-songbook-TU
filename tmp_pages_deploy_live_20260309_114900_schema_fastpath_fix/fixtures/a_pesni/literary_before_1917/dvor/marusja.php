   В 1920-е гг. песня вновь издавалась на пластинках 
  в дореволюционной записи С.П. Садовникова.<br>
  <br>
  Сюжет получил развитие в песенке про безуспешные попытки вылечить Марусю и ее 
  отправку на кладбище - <a href="marusja-vetch.php">&quot;Маруся&quot;</a> (&quot;Вечер 
  вечереет...&quot;) и в пародии конца 1920-х гг. <a href="16nozej.php">&quot;Шестнадцать 
  столовых ножей&quot;</a> (&quot;Жила-была Маруся...&quot;). На основе &quot;Маруся 
  отравилась&quot; не позднее 1921 г. появилась песня об убитом в драке хулигане 
  <a href="arzak.php">&quot;Аржак&quot;</a> (она же &quot;Чеснок&quot; - в разных 
  версиях герой носит разное имя).<br>
<br>
  См. также близкую песню <a href="../popular20/kolja-manja.php">&quot;Гуляли 
  Коля с Манею&quot;</a>, где парень бьет девушку за отказ выйти за него, и та 
  умирает в больнице.<br>
  <br>
 Такое же название носит сложенная другим поэтическим размером песня из репертуара Юрия Морфесси (1882-1957) 
  - см. <a href="marusja-zizn.php">&quot;Маруся отравилась&quot; (Жизнь так несложна, 
  крайне так проста...)</a>. Запись на пластинку - фирма &quot;Зонофон&quot;, 
  Петербург, 1912 г., Х-3-62119. <br>
  <br>
               Подробнее об истории и варианты песни см.: Сергей Неклюдов <a href="moskva/a-marusia.php">&quot;Почему 
        отравилась Маруся?&quot;</a> (2008).<br>
