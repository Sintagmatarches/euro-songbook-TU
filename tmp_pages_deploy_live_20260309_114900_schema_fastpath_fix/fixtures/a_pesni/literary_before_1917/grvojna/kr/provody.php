        Стихотворение Демьяна Бедного написано в 1918 г. на фронте в Свияжске. В 1928 г. композитор Дмитрий Васильев-Буглай приспособил к нему напев украинской песенки  <a href="../../ukr/ojschotam.php">&quot;Ой, 
        що ж то за шум учинився&quot;</a> (она же &quot;Комарик&quot;). Это сделано было для инсценировки &quot;Диво-дивное&quot; по басням Д. Бедного в пехотной школе им. ВЦИК. Напев &quot;Комарика&quot; в русской армии использовался на Первой мировой войне, зафиксирован в «Песнях нашей армии» С. Орлова и Ф. Щеглова, изд. Юргенсона, 1915. См.: Нестьев И.В. Массовая песня // Очерки советского музыкального творчества. Т. 1. М.; Л.: Музгиз, 1947. С. 241.<br>
        <br>&quot;Комарик&quot; известна также у русских цыган (&quot;Комаричко&quot;). 
        У украинцев поется о том, как комарик женился на мухе, а потом свалился с дуба, а у цыган он сразу свалился с дуба, а мушки его подняли. Украинская песня была записана 
        на пластинку Петром Лещенко (фирма &quot;Колумбия&quot;, 1936-1937 гг., 
        WHR663, &quot;Комарик&quot;, украинская шуточная песня). <br>
