  <em>Граф Владимир Соллогуб никогда не был военным. Подобно Федору Тютчеву, он 
  защищал интересы России на дипломатическом поприще в Австрии (Тютчев, в те же 
  самые годы, - в Германии). Но именно Соллогубу принадлежат несколько "военных 
  песен", получивших широкий резонанс. Начало Крымской войны отмечено одой-симфонией 
  на его стихи "Россия перед врагами", широкую известность приобрела песня "Алла 
  верды", в которой нашла отражение кавказская война. Упоминаемая в песне "иверская 
  земля" - древняя Грузия Иверия). Азарпеша - чаша; Тулумбаш - тамада. Обычай 
  принимать любого гостя как "дарованного Богом" тоже принадлежит к числу основных 
  достопримечательностей Кавказа. Следуя ему, Ермолов, будучи "проконсулом Кавказа", 
  ввел свой: к нему на обед мог прийти любой, буквально "с улицы". Таковым было 
  "алла верды" русского генерала. <br>
  <br>
  Антология военной песни / Сост. и автор предисл. В. Калугин. 
  М.: Эксмо, 2006.</em><br>
 <br>
