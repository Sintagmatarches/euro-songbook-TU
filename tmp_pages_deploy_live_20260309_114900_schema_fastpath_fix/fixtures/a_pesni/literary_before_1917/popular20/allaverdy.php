  Песня была очень популярна в военной среде, вплоть до эпохи Гражданской войны, 
  когда пелась в Белом движении. Сцена исполнения песни офицерами-каппелевцами 
  в поезде была  снята братьями Васильевами для фильма &quot;Чапаев&quot; 
  (1934), но по требованию цензуры вырезана. <br>
