<br>
Бежавший с сибирского края,<br>
Он вырвался с звонких цепей.<br>
Старик-арестант изнуренный<br>
К семье возвратился своей.<br>
<br>
Спешит он семейство увидеть,<br>
И милого сына обнять,<br>
Жену приласкать и утешить<br>
Старушку - родимую мать.<br>
<br>
Пришел он в убого жилище -<br>
Родных нету в нем никого.<br>
Другая семья там ютится,<br>
Другие встречают его:<br>
<br>
И стиснуло сердце тревожно:<br>
- Вернулся я, видно, не в срок.<br>
Скажите вы мне, ради Бога,<br>
Где мать, где жена, где сынок?<br>
<br>
- Садись, успокойся немного,<br>
Наверно, ведь ноги болят.<br>
Ты твердо мужайся и слушай,<br>
Скажу, где родимая мать.<br>
<br>
Он сел, успокоился: - Где же?<br>
А сам весь дрожит, точно лист.<br>
Он слушал печальные вести,<br>
И слезы невольно лились.<br>
<br>
- Твоя мать, тревожась о сыне,<br>
Семь лет как уже умерла. <br>
Не знает тревоги, кручины -<br>
В могиле покой обрела.<br>
<br>
<em>Принесена Антониной Анатольевной Строковой из Ярославля, датирована 
1925 годом.<br>
<br>
Ярославская областная газета <a href="http://www.goldring.ru">«Золотое кольцо»</a>, 
12.03.2003, рубрика «Песни нашего двора». </em><br>
