<h1>ДРУГ МОЙ 
МИЛЫЙ </h1>
	<br>
	При бурной ноченьке холодной<br>
  Скрывался месяц в облака.<br>
  На ту на самую могилку<br>
  Пришла красавица моя.<br>
  <br>Взошла на самый край вершины,<br>
  Главой на памятник легла,<br>
  Сказала: - Господи, помилуй! –<br>
  И она тут же обмерла.<br>
  <br>Она унылым голосочком<br>
  Будила друга своего:<br>
  - Ах, встань, проснися, друг мой милый,<br>
  Я без тебя жить не могу!<br>
  <br>И вдруг могила задрожала,<br>
  Из гроба голос раздался:<br>
  - Ах, отойди ты, негодяйка,<br>
  И не тревожь моих костей!<br>
  <br>Пошла девчонка, зарыдала,<br>
  Бранила друга своего:<br>
  - Ах, и зачем было влюбляться<br>
  И золото кольцо дарить!<br>
  <br>
	Ах, мое золото колечко –<br>
  Оно всегда, как жар, горит,<br>
  А мое бедное сердечко,<br>
  Оно всегда ноет, болит!<br>
  <br>
