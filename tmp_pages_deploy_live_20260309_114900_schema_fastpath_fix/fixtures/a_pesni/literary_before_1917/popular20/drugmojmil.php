	 <br>
	  При бурной ночи, тихой, хладной<br>
	  Скрывался месяц в облаках.<br>
      На ту зелёную могилку<br>
      Пришла красотка вся в слезах.<br>
      <br>
      Она пришла на край могилы,<br>
      Главой на памятник легла.<br>
      Своим унылым голосочком<br>
      Будила друга своего:<br>
      - Восстань, восстань, мой друг любезный,<br>
      Я без тебя жить не могу.<br>
      <br>
      Вдруг могила задрожала<br>
      И громко голос подаёт:<br>
      - Уйди, уйди ты, дорогая,<br>
      Не тревожь моих костей.<br>
      <br>
      Мои кости призастыли,<br>
      Один холодный труп лежит.<br>
      Уж наколе так влюбляться,<br>
      Золотым кольцом дарить.<br>
      <br>
      Твоё колечко не пропало,<br>
      Оно в коробочке лежит.<br>
      Моё сердечко не спокойно,<br>
      Но всё по милому грустит.<br>
      <br>
