		<h1>КАТЕНЬКА-КАТЮША...</h1>
<br>
Катенька-Катюша,<br>
Купецкая дочь,<br>
Где ты прогуляла<br>
Всю тёмную ночь.<br>
<br>
Рано на рассвете<br>
Ложилася спать.<br>
Пришла её мать,<br>
И стала ругать.<br>
<br>
- Катенька-Катюша,<br>
Пора уж вставать.<br>
Пришли пароходы,<br>
На море стоят.<br>
<br>
Один парус белый,<br>
Другой голубой.<br>
Один матрос старый,<br>
Другой молодой.<br>
<br>
- Катенька-Катюша,<br>
Поедем со мной,<br>
Куплю тебе платье,<br>
Платок голубой.<br>
<br>
За крутые горы,<br>
За тёмные леса.<br>
Где молнце не светит,<br>
Луна никогда.<br>
<br>
