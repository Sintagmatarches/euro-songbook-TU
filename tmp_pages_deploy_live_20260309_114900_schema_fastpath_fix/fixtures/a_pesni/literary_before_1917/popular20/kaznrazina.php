<strong>АВТОРСКОЕ СТИХОТВОРЕНИЕ <br>
<br> 
Казнь Стеньки Разина
</strong><br>
<br>
Слова Ивана Сурикова <br>
<br>
Точно море в час прибоя,<br>
Площадь Красная гудит.<br>
Что за говор? что там против<br>
Места лобного стоит? <br>
<br>
Плаха черная далеко<br>
От себя бросает тень...<br>
Нет ни облачка на небе…<br>
Блещут главы… Ясен день. <br>
<br>
Ярко с неба светит солнце<br>
На кремлевские зубцы,<br>
И вокруг высокой плахи<br>
В два ряда стоят стрельцы. <br>
<br>
Вот толпа заколыхалась, -<br>
Проложил дорогу кнут:<br>
Той дороженькой на площадь<br>
Стеньку Разина ведут. <br>
<br>
С головы казацкой сбриты<br>
Кудри черные как смоль;<br>
Но лица не изменили<br>
Казни страх и пытки боль. <br>
<br>
Так же мрачно и сурово,<br>
Как и прежде, смотрит он, -<br>
Перед ним былое время<br>
Восстает, как яркий сон: <br>
<br>
Дона тихого приволье,<br>
Волги-матушки простор,<br>
Где с судов больших и малых<br>
Брал он с вольницей побор; <br>
<br>
Как он с силою казацкой<br>
Рыскал вихорем степным,<br>
И кичливое боярство<br>
Трепетало перед ним. <br>
<br>
Душит злоба удалого,<br>
Жгет огнем и давит грудь,<br>
Но тяжелые колодки<br>
С ног не в силах он смахнуть. <br>
<br>
С болью тяжкою оставил<br>
В это утро он тюрьму:<br>
Жаль не жизни, а свободы,<br>
Жалко волюшки ему. <br>
<br>
Не придется Стеньке крикнуть<br>
Клич казацкой голытьбе<br>
И призвать ее на помощь<br>
С Дона тихого к себе. <br>
<br>
Не удастся с этой силой<br>
Силу ратную тряхнуть –<br>
Воевод, бояр московских<br>
В три погибели согнуть. <br>
<br>
«Как под городом Симбирском<br>
Думу думает Степан, -<br>
Рать казацкая побита,<br>
Не побит лишь атаман. <br>
<br>
Знать, уж долюшка такая,<br>
Что на Дон казак бежал,<br>
На родной своей сторонке<br>
Во поиманье попал. <br>
<br>
Не больна мне та обида,<br>
Та истома не горька,<br>
Что московские бояре<br>
Заковали казака, <br>
<br>
Что на помосте высоком<br>
Поплачусь я головой<br>
За разгульные потехи<br>
С разудалой голытьбой. <br>
<br>
Нет, мне та больна обида,<br>
Мне горька истома та,<br>
Что изменою, не правдой<br>
Голова моя взята! <br>
<br>
Вот сейчас на смертной плахе<br>
Срубят голову мою,<br>
И казацкой алой кровью<br>
Черный помост я полью… <br>
<br>
Ой, ты, Дон ли мой родимый!<br>
Волга-матушка река!<br>
Помяните добрым словом<br>
Атамана казака!..» <br>
<br>
Вот и помост перед Стенькой…<br>
Разин бровью не повел.<br>
И наверх он по ступеням<br>
Бодрой поступью взошел. <br>
<br>
Поклонился он народу,<br>
Помолился на собор…<br>
И палач в рубашке красной<br>
Высоко взмахнул топор… <br>
<br>
«Ты прости, народ крещеный!<br>
Ты прости-прощай, Москва!..»<br>
И скатилась с плеч казацких<br>
Удалая голова. <br>
<br>
