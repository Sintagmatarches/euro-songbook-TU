<strong>ВАРИАНТЫ (2)<br>
<br>
1. Словно море в час прибоя</strong><br>
<br>
Словно море в час прибоя, площадь Красная гудит.<br>
Что за говор? Что там против места лобного стоит? <br>
Плаха черная далеко от себя бросает тень,<br>
Нет ни облачка на небе, блещут главы, светел день.<br>
Вдруг толпа заколыхалась, проложил дорогу кнут <br>
Той дороженькой на плаху Стеньку Разина ведут! <br>
С головы казацкой сбриты кудри черные, как смоль, <br>
Но лицо не изменили казни страх и пытки боль, <br>
Дона тихое раздолье, Волги-матушки простор, <br>
Где с судов больших и малых <br>
Брал он с вольницей побор, <br>
Как он с вольницей казацкой рыскал вихрем степовым,<br>
И кичливое боярство трепетало перед ним. <br>
Вот и плаха перед Стенькой... Разин бровью не повел<br>
И наверх он по ступенькам бодрой поступью взошёл<br>
Поклонился он народу, перекрестился на собор, <br>
И палач в рубахе красной высоко занес топор…<br>
«Ты прости, мой Дон родимый, <br>
Волга-матушка-река, <br>
Помяните добрым словом атамана-казака!» <br>
