И пред ним былое время<br>
Восстает, как яркий сон:<br>
Дона тихого приволье,<br>
Волги-матушки простор,<br>
Как с больших судов и с малых<br>
Брал он с вольницей побор,<br>
По степям богатым волжским<br>
Рыскал вихрем он степным,<br>
И кичливое боярство<br>
Трепетало перед ним.<br>
Как над городом Симбирском<br>
Думу думает Степан,<br>
Рать казацкая побита,<br>
Не побит лишь атаман.<br>
Знать, уж долюшка такая,<br>
Что не пал казак в бою,<br>
А сберег для черной плахи<br>
Буйну голову свою.<br>
Знать, уж долюшка такая,<br>
Что на Дон казак бежал<br>
И в родной своей сторонке<br>
Во поймание попал.<br>
Не обидна Стеньке доля,<br>
Та истома не горька,<br>
Что московские бояре<br>
Заковали казака.<br>
А обидна Стеньке доля<br>
И горька истома та,<br>
Что изменою-неправдой<br>
Голова его взята.<br>
Душу злого-удалого<br>
Жжет огнем и давит грудь,<br>
Но тяжелые колодки<br>
Он не в силах с ног смахнуть.<br>
«Что сейчас на черной плахе<br>
Срубят голову мою,<br>
И казацкой алой кровью<br>
Черный помост я полью».<br>
