Разин бровью не повел,<br>
И наверх он по ступенькам<br>
Гордой поступью взошел.<br>
Поклонился он народу,<br>
Помолился на собор,<br>
И палач в рубашке красной<br>
Широко взмахнул топор.<br>
«Ты прости, народ крещеный,<br>
Ты прости-прощай, Москва».<br>
