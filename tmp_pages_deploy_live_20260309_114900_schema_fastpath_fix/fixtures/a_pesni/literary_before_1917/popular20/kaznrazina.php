<strong>2. Точно море в час прибоя</strong><br>
<br>
Точно море в час прибоя,<br>
Площадь Красная горит.<br>
Что за говор, что там против<br>
Место лобное стоит.<br>
Плаха черная далеко<br>
От себя бросает тень.<br>
Нет ни облачка на небе,<br>
Блещут главы, ясен день.<br>
Солнце с неба светит ярко<br>
На кремлевские зубцы.<br>
И вокруг высокой плахи<br>
В два ряда стоят стрельцы.<br>
Вот толпа заколыхалась,<br>
Проложил дорогу кнут.<br>
Той дороженькой на плаху<br>
Стеньку Разина ведут.<br>
С головы казацкой сбриты<br>
