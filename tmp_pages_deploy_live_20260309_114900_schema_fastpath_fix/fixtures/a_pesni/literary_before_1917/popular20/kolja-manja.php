	<span class="header"><strong>ГУЛЯЛИ КОЛЯ 
С МАНЕЮ </strong><br></span>
<br>
Гуляли Коля с Манею, а Маня хороша.<br>
        Просватал Коля Манечку, а Маня не пошла.<br>
  Коля после этого такой вопрос решал:<br>
  Убить или зарезать он Маню обещал.<br>
<br>
Подружка услыхала и Мане говорит:<br>
  «Тебя Колечка хочет зарезать или убить».<br>
  Манечка подружке с грустью говорит:<br>
  «Теперь я перестану на улицу ходить».<br>
<br>
Коля на гуляночку, а Маня не пришла.<br>
  Любимая подружка за Маней не зашла.<br>
  Колечка подружке строго говорит:<br>
  «Иди скорей за Манею, скажи, Коля велит».<br>
<br>
Подружка повернулась, за Манею пошла,<br>
  А Манечка печальная сидела у окна.<br>
  Народу было мало, огонь едва горел:<br>
  «Пойдем, Маня, на улицу, тебе Коля велел!»<br>
<br>
Коля – по дороженьке, а пыль ему в лицо,<br>
  Манечка с подружкою выходят на крыльцо.<br>
  «Колечка ты, Коля, зачем ты мне велел?» -<br>
  «За измену, Манечка, - убить тебя хотел».<br>
<br>
«Не бей ты меня, девицу, не бей и не тревожь.<br>
  За меня, за девицу, в тюрьме ты пропадешь». –<br>
  «Убью тебя я, девицу, убью и буду знать:<br>
  За тебя, за девицу, в тюрьме буду страдать».<br>
<br>
Ударил он Маню, попал ей в голову,<br>
  Манечка упала на пыльную траву.<br>
  Подружка закричала: «Подайте лошадей!»,<br>
  Чтоб увести Маню в больницу поскорей.<br>
<br>
В больницу привозили и клали на кровать,<br>
  Давали сонных капель, велели крепко спать.<br>
  Маня эти капли совсем уж не брала,<br>
        А через полчасочка Маня умерла.<br>
<br>
Манечка нарядная лежала на столе,<br>
  На ней белое платье, венок на голове.<br>
  Манечка в могиле зарыта глубоко,<br>
  А Коля за решеткою, ему там нелегко.<br>
<br>
Над Маниной могилой соловей поет,<br>
  Манечка на улицу больше не придет…<br>
