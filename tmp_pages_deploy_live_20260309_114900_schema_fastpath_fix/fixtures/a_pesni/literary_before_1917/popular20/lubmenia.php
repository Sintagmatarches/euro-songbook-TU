	Любила меня мать, уважала...<br></span>
<br>
	Любила меня мать, уважала,<br>
Что я ненаглядная дочь.<br>
	А дочка с тоски убежала<br>
В осеннюю тёмную ночь.<br>
<br>
Бежала я лесом дремучим,<br>
Бежала я горой крутой,<br>
Вздохнула я, взглянула на небо<br>
И вспомнила родную мать.<br>
<br>
И вспомнила рощу зелёную,<br> 
И вспомнила цвет голубой.
<br>
И вспомнила я милого речи,<br>
Залилась я горькой слезой.<br>
<br>
Подайте мне коробочку спичек,<br>
На тёплой воде я разведу.<br>
Сыграйте вы, девушки, разлуку,<br>
На сердце отраву я приму.<br>
<br>
Отрава моя недорогая,<br>
Не купишь её за пятак,<br>
А жизнь моя дорогая,<br>
Не купишь её за так.<br>
<br>
