		<h1>ЛЮБИЛА 
МЕНЯ МАТЬ</h1>
Бытовой романс <br>
<br>
Любила меня мать, обожала,<br>
  Что я ненаглядная дочь.<br>
  А дочка с другим убежала<br>
  В осеннюю темную ночь.<br>
<br>
Бежала я лесом дремучим,<br>
  Бежала я рощей густой,<br>
  Взглянула на небо, вздохнула<br>
  И вспомнила дом свой родной!<br>
<br>
На что мне зеленая роща,<br>
  На что мне родительский дом?<br>
  Как вспомню я милого речи,<br>
  Зальюся я горькой слезой.<br>
<br>
Куплю я коробочку спичек<br>
  И в теплой воде разведу,<br>
  И долго я думать не стану,<br>
  Сейчас я отраву приму.<br>
<br>
Отрава недорогая,<br>
  Всего только стоит пятак,<br>
  А жизнь ты, проклятая, злая,<br>
  Сейчас загублю тебя так.<br>
<br>
Подруга подруге сказала:<br>
  Прими же отравку и ты!<br>
  Со свету меня ты согнала,<br>
  Умри же, несчастная, ты!<br>
<br>
Подруги мои, приходите,<br>
  Я буду лежать на столе,<br>
  Прошу вас, меня не судите,<br>
  Заройте мой труп в тишине.<br>
<br>
<span class='i'>Из репертуара Юрия Морфесси (1882-1957)<br>
  <br>
  Очи черные: Старинный русский романс. М.: Эксмо, 2004.</span><br>
  <br>
<br>
Известны переработки времен Первой мировой (<a href="../ww1/lubmenjamat.php">&quot;Любила 
меня мать, обожала&quot;</a> ) и Гражданской войны (<a href="../grvojna/kr/lubilauvazala.php">&quot;Любила меня мать, уважала&quot;</a>) о возвращении искалеченного солдата домой. Соответственно, 
сам романс появился до Первой мировой войны и в предвоенное время был популярен.<br>
