Не рассказать ли вам, ребята,<br>
	  О двух удалых молодцах?<br>
	  Глубоким сном тюрьма объята,<br>
	  Не слышно шума голосов,<br>
	  Не слышно песни запоздалой,<br>
	  Души не видно у окна.<br>
	  Лишь два кандальщика удалых,<br>
	  Они не спят, им не до сна.<br>
	  В эту ночь они решили<br>
	  На вольной воле побывать.<br>
	  - Довольно мы здесь посидели,<br>
	  Пора и совести нам знать.<br>
	  Начальству мы уж надоели -<br>
	  Кормить, поить и обувать,<br>
	  Брить головы, сменять постели,<br>
	  Оковы на ноги ковать.<br>
	  С начальством надо распроститься,<br>
	  За хлеб и соль благодарить.<br>
	  И с дядей Яшей распроститься,<br>
	  да жаль будить, он крепко спит.<br>
	  К побегу все было готово:<br>
	  В решетке сделан был прорез.<br>
	  Упали с ног долой оковы<br>
	  И смело двинулись вперед.<br>
	  На утро сделалась тревога<br>
	  По всей тюрьме пошла молва,<br>
	  Что из проклятого Централа<br>
	  Ушли два юных молодца.<br>
	  Ищите в поле ветер вольный!<br>
	  Дай в волю смело погулять<br>
	  И проклятого Централа<br>
	  Чтоб им больше не видать!<br><br>
