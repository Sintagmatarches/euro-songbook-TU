	      Эту же песню зафиксировала в неназванной калужской деревне В.Н. Шацкая в 1922-25 гг. (Шацкая В. Песни и быт деревни (Из работ I опытной станции Наркомпроса) // Вестник просвещения: Ежемес. общественно-пед. журнал Моск. отдела нар. образ. и Моск. Губроса.  1925. №6. С. 78-89). Эта деревня жила за счет заработков в Москве и Петербурге. Т.е. в 1910-1920-х гг. песня была общим знанием в России. Шацкая, поверив местным исполнителям, ошибочно отнесла песню  к 1-й п. XIX в.:<br>
  <br>
