	<h1>СВИСТИТ СВИСТОК ДАЛЕКИЙ...</h1>
	<br>
	Свистит свисток далекий,<br> 
	Свистка мне не слыхать,<br>
	А пароход уехал,<br> 
	Милого не видать.<br>
	<br> 
	Уехал мил в деревню,<br>
	Подарочек прислал -<br> 
	Колечко золотое,<br>
	Букет из белых роз.<br> <br>
	Колечко распаялось,<br>
	Букет отцвел, завял.<br>
	Наверно, мой хороший<br> 
 Другую полюбил.<br>
 <br> 
 Другую, мил, полюбишь,<br> 
 В ней счастья не найдешь.<br> 
 Меня ты позабудешь<br> 
 И ночки не уснешь.<br> 
 <br> 
 Мил редко навещает,<br>
  Пойду к нему сама.<br>
  Иду, а ветер дует,<br> 
   Едва на свет гляжу.<br> 
 <br> 
 К крылечку подходила<br> 
 И стала у дверей.<br> 
 Ретивое сердечко<br> 
 Забилося сильней.<br>
  <br>
  И что же, почему же<br> 
   Я буду здесь стоять,<br> 
 И что же понапрасну<br> 
 Здесь время провождать?<br> 
 <br> 
 Дрожащею рукою<br> 
 Я дернула звонок,<br>
  И вдруг передо мною<br>
  Отщелкнулся замок.<br> 
   <br> 
 Служанка выходила,<br> 
 Мне ручку подала<br> 
 И с гордою улыбкой<br> 
 По комнатам вела.<br> 
 <br>
  Все комнаты открыты,<br>
  Одна лишь заперта,<br>  
  И где измена скрыта,<br> 
 К той двери подвела.<br> 
 <br> 
 &quot;Бери смелей за ручку&quot;, - <br>
 Сказала она мне.<br> 
 Я тихо дверь открыла,<br>
  Служанка прочь пошла.<br>
  <br> 
   Я тихо дверь открыла,<br>
Служанка прочь пошла.<br> 
 Я на порог ступила,<br> 
 А дальше не смогла.<br> 
 <br>
  Сидит мил на диване,<br>
  Я в ужасти стою.<br> 
   Он держит на коленях<br> 
 Соперницу мою.<br> 
 <br> 
 &quot;Мой милый, не пугайся,<br>
  Ведь это я пришла.<br>
  Красотка, не влюбляйся,<br> 
   Изменит он тебя!<br> 
 <br>
 Красотка, не влюбляйся,<br>
Изменит он тебя,<br>
  Изменит и спокинет,<br> 
   Как изменил меня&quot;.<br> 
