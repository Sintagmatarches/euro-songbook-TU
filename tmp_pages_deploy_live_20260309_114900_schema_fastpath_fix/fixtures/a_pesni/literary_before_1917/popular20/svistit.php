      Ах, милый мой дружочек,<br>
      Далёко ты живёшь.<br>
      А писем мне не пишешь,<br>
      Поклончику не шлёшь.<br>
      <br>
      Приехал твой товарищ,<br>
      Подарочек привёз,<br>
      Колечко золотое,<br>
      Букет из алых роз.<br>
      <br>
      Колечко распаялось,<br>
      Букетик призавял.<br>
      Да милый мой дружочек<br>
      Любить другую стал.<br>
      <br>
      Да милый мой дружочек<br>
      Любить другую стал.<br>
      Другую, не чужую -<br>
      Подруженьку мою.<br>
      <br>
      Подруга ты, подруга,<br>
      Обманет он тебя,<br>
      Обманет и погубит,<br>
      Как загубил меня.<br>
      <br>
      Люби, подруга, друга,<br>
      Люби его, как я.<br>
      Стирай ему платочек,<br>
      Выглаживай, как я.<br>
