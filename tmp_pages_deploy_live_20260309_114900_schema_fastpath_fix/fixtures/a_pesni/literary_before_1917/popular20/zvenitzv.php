	<span class="header"><strong>ЗВЕНИТ ЗВОНОК НАСЧЕТ ПОВЕРКИ </strong><br></span>
  <br>
  Звенит звонок насчет поверки, <br>
  Ланцов задумал убежать. <br>
  Не стал зари он дожидаться, <br>
  Проворно начал печь ломать.<br>
  <br>
  В трубу он тесную пробрался <br>
  На тот тюремный на чердак. <br>
  По чердаку он долго шлялся, <br>
  Себе веревочку искал.<br>
  <br>
  Нашел веревку тонку длинну, <br>
  К трубе тюремной привязал, <br>
  Перекрестился, стал спускаться, <br>
  Солдат заметил, выстрел дал.<br>
  <br>
  Казак на серенькой лошадке <br>
  С доносом к князю поскакал.<br>
  - Я к вашей милости с докладом:<br>
  Ланцов из замка убежал.<br>
  <br>
  <em>Песни узников. Составитель Владимир Пентюхов. Красноярск: Производственно-издательский 
  комбинат &quot;ОФСЕТ&quot;, 1995. </em><br>
 <br>
  <br> 
  <strong>ВАРИАНТ <br>
  <br> 
  Звенит звонок на том собраньи... </strong><br>
  <br> Звенит звонок на том собраньи,<br>
	  Лонцов задумал отдохнуть.<br>
	  Лонцов зари не дожидался<br>
	  Проворно печку стал ломать.<br>
	  Он в трубу тесну ворвался<br>
	  И на тюремный на чердак.<br>
	  Перекрестился, стал спускаться.<br>
	  Часовой услышал, выстрел дал.<br>
	  В команде сделалась тревога,<br>
	  На фрунте пробил барабан.<br>
	  По всем частям было известно<br>
	  Лонцов из замка убежал.<br>
	  Бежал знакомою дорогой<br>
	  И своротил направо в лес,<br>
	  С одною девой повстречался,<br>
	  С которой ране три года жил. <br>
      <strong><br>
      <em>Гуревич А.В., Элиасов Л.Е. Старый фольклор Прибайкалья. Том первый. Улан-Удэ, 1939. С. 1-2. Раздел «Бродяжьи тюремные песни», №4. С прим. (с. 443-445):</em></strong><em><br>
      </em><br> 
  <em>Текст  записан т. Элиасовым Л.Е. со слов Кожевина Л.Б., в с. Суво, Баргузинского аймака, БМАССР. <br>
  <br> Песня о Ланцове одна из популярнейших тюремных песен. О Ланцове и истории создании этой песни см. Львов Е. - «В Сибирь на каторгу». Повесть. Характеристики и арабески. Москва, 1876 г., гл. «Пересылочные вообще и Ланцов в особенности», стр. 102-103. <br>
  <br> Вариант этой песни записан в 1934 г. А.М. Астаховой в с. Тарбагатае, БМАССР: <br>
  <br> Прабил-то звонок нащет паверки,<br>
	  Ланцов ли задумал убежать: (2 раза)<br>
	  Не стал лё он свету дожидаться,<br>
	  Трубу ле он-то тесною стал ламать. (2 раза)<br>
	  В трубу ле он-то теснаю он прабрался,<br>
	  На тот ле на теремнай на чердак. (2 раза)<br>
	  Па чердакам-то он долго шлялся,<br>
	  Сибе ле веревочку-то искал. (2 раза)<br>
	  Нашол ле веревку, стал опускаться.<br>
	  Солдат ле увидил, выстрил дал. (2 раза)<br>
	  Солдат ле на сераю лошадке<br>
	  К царю с канвертам пабижал,<br>
	  К царю ли с канвертом пабижал.<br>
	  Бежал та, скакал петровским трахтом,<br>
	  Сваротил он в темный лес.<br>
	  В лесу ли он три годы жил.<br>
	  Чё-то он хотел, то он пил и ел,<br>
	  Расой-то он умывался,<br>
	  А на восток богу молился. <br>
  <br> (Исполнительница песни т. Ефимова сообщила Астаховой дополнительно следующее: «Старинная. Ланцов был разбойник. Шибко удалый». «Его нице не брало: ни замки, ни пули. Тюрьма не держала». «Ее очень давно пели. Да и теперь поет середняк, не молодежь». <br>
  <br> Записано А.М. Астаховой в с. Тарбагатае, БМАССР, 27 июля 1934 г. от колхозницы Евдокии Дементьевны Ефимовой, 39 л. (Рукописное хранилище фольклорной секции Института антропологии и этнографии Академии наук СССР). <br>
  <br> (Проф. Н.П. Андреев - «Русский фольклор», хрестоматия, 1936 г. стр. 156-157). <br>
  <br> Из ранних записей песни о Ланцове приводим два следующих варианта: <br>
  <br> I. Ланцов сидел в тюремном замке,<br>
	  Потом задумал убежать:<br>
	  Не стал зари он дожидаться,<br>
	  Проворно печку стал ломать.<br>
	  В трубу он тесную пробрался,<br>
	  На церковный на чердак;<br>
	  В трубе увязывал, трясся...<br>
	  В ограду страсть было смотреть...<br>
	  В казармах сделалась тревога:<br>
	  Ланцов из замка убежал...<br>
	  По всем частям было известно -<br>
	  Ланцов продал барабан... <br>
  <br> (Н.М. Костюрина «Сибирские народные песни», записанные в подгородных деревнях около Тобольска летом 1894 года. С приложением некоторых мелодий (С примечаниями члена редакционной комиссии Л.Е. Луговского).  <br>
  <br> «Ежегодник Тобольского губернского музея»,  1895 г., выпуск III, стр. 58-59, текст №91. <br>
  <br> II. Звонок звенит,<br>
	  На счет повестки он дает.<br>
	  Не стал я зори дожидаться,<br>
	  Проворно печку стал ломать.<br>
	  В трубу в тесную ворвался,<br>
	  На тот церковный на чердак,<br>
	  С ограды страсть ему взглянуть.<br>
	  Ему пришло на размышленье<br>
	  Последний раз на дверь взглянуть.<br>
	  Прощайте, друзья моему сердцу,<br>
	  Прощай, заглавная абвахта,<br>
	  Прощай, тюремный коридор,<br>
	  Прощай, мой товарищ Александр.<br>
	  Перекрестился, сам спустился.<br>
	  Солдат увидел, закричал,<br>
	  В казарме сделалась тревога,<br>
	  Во фронте бьют барабан.<br>
	  Ланцов из замка убежал<br>
	  Сибирским трактом,<br>
	  Вернул в дремучий лес.<br>
	  Три годочка шлялся,<br>
	  Не знал, чо пил он, ел.<br>
	  С крестьянской девушкой сознался,<br>
	  Со старопрежней со своей.<br>
	  Крестьянска девушка<br>
	  Настоль была красива,<br>
	  Ланцов задумал ободрать.<br>
	  Снимал косинку и перчатки,<br>
	  С руки жемчужное кольцо.<br>
	  Крестьянска девушка не стерпела:<br>
	  Ланцова взяли под надзор.<br>
	  В семи душаз Ланцов сознался,<br>
	  Восьму хотел он погубить.<br>
	  За то Ланцова взяли,<br>
	  Что в старо место жить попал. <br>
  <br> (Песня записана А.В. Жилинской в дер. Окатьевой, Иковской  волости, Томской губернии). <br>
  <br> (Записки Красноярского подотдела Восточно-Сибирского Отдела Русского Географического Общества». Том I-й, выпуск I-й, Красноярск, 1902 г., текст песни №71, стр. 165-166). <br>
  <br> Варианты песни о Ланцове, записанные в 1936 г. в Усольском районе и Братском районе Иркутской области, см. в сборнике А.В. Гуревича «Фольклор Восточной Сибири», ОГИЗ, Иркутск, 1938 г., текст №43, стр. 14, вариант к тексту №43, стр. 158. <br>
