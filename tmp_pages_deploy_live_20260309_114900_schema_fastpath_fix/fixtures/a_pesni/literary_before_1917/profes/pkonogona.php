  <br>
  <strong>6. Коногон </strong><br>
  <br>
  Гудки тревожно загудели,<br>
  Народ валит густой толпой.<br>
  А молодого коногона<br>
  Несут с разбитой головой. <br>
  <br>
  — Зачем ты, парень, торопился,<br>
  Зачем коня так быстро гнал?<br>
  Или десятника боялся,<br>
  Или в контору задолжал? <br>
  <br>
  — Десятника я не боялся,<br>
  В контору я не задолжал.<br>
  А мне забойщики кричали,<br>
  Чтоб порожняк быстрее гнал. <br>
  <br>
  О шахта, шахта, ты — могила,<br>
  Зачем ты мужа забрала?<br>
  Моих детей осиротила,<br>
  Меня вдовою назвала? <br>
  <br>
  Ох, шахта, шахта, ты — могила.<br>
  Зачем сгубила ты меня?<br>
  Прощайте, все мои родные, —<br>
  Вас не увижу больше я. <br>
  <br>
  В углу заплачет мать-старушка.<br>
  Слезу рукой смахнет отец.<br>
  И дорогая не узнает,<br>
  Каков шахтера был конец. <br>
  <br>
  И будет карточка пылиться<br>
  На полке пожелтевших книг<br>
  В танкистской форме при погонах<br>
  И ей он больше не жених. <br>
  <br>
  Прощай, Маруся ламповая,<br>
  И ты братишка стволовой.<br>
  Тебя я больше не увижу —<br>
  Лежу с разбитой головой. <br>
  <br>
  Гудки тревожно загудели,<br>
  Народ валит густой толпой.<br>
  А молодого коногона<br>
  Несут с разбитой головой. <br>
  <br>
  Вот лошадь мчится по продольной,<br>
  По темной, узкой и сырой,<br>
  А коногона молодого<br>
  Предупреждает тормозной: <br>
  <br>
  «Ах, тише, тише, ради Бога!<br>
  Здесь ведь и так большой уклон.<br>
  На повороте путь разрушен,<br>
  С толчка забурится вагон». <br>
  <br>
  И вдруг вагончик забурился,<br>
  Беднягу к парам он прижал,<br>
  И к коногону молодому<br>
  Друзей на помощь кто-то звал. <br>
  <br>
  Через минуту над вагоном<br>
  Уже стоял народ толпой,<br>
  А коногона к шахтной клети<br>
  Несли с разбитой головой. <br>
  <br>
  «Ах, глупый, глупый ты мальчишка.<br>
  Зачем так быстро лошадь гнал?<br>
  Или начальства ты боялся,<br>
  Или конторе угождал?» <br>
  <br>
  «Нет, я начальства не боялся,<br>
  Конторе я не угождал, —<br>
  Мне приказал начальник шахты,<br>
  Чтоб порожняк быстрей давал. <br>
  <br>
  Прощай навеки, коренная,<br>
  Мне не увидеться с тобой,<br>
  Прощай, Маруся, ламповая,<br>
  И ты, братишка стволовой. <br>
  <br>
  Я был отважным коногоном,<br>
  Родная маменька моя,<br>
  Меня убило в темной шахте,<br>
  А ты осталася одна». <br>
