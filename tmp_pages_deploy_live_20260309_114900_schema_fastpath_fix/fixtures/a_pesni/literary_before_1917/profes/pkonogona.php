	<span class="header"><strong>ПЕСНЯ О КОНОГОНЕ</strong><br></span>
  <br>
  Вот лошадь мчится по продольной,<br>
  По темной, узкой и сырой,<br>
  А коногона молодого<br>
  Предупреждает тормозной:<br>
  <br>
  «Ах, тише, тише, ради бога!<br>
  Здесь ведь и так большой уклон.<br>
  На повороте путь разрушен,<br>
  С толчка забурится вагон».<br>
  <br>
  И вдруг вагончик забурился,<br>
  Беднягу к парам он прижал,<br>
  И к коногону молодому<br>
  Друзей на помощь кто-то звал.<br>
  <br>
  Через минуту над вагоном<br>
  Уже стоял народ толпой,<br>
  А коногона к шахтной клети<br>
  Несли с разбитой головой.<br>
  <br>
  «Ах, глупый, глупый ты мальчишка.<br>
  Зачем так быстро лошадь гнал?<br>
  Или начальства ты боялся,<br>
  Или конторе угождал?»<br>
  <br>
  «Нет, я начальства не боялся,<br>
  Конторе я не угождал, -<br>
  Мне приказал начальник шахты,<br>
  Чтоб порожняк быстрей давал.<br>
  <br>
  Прощай навеки, коренная,<br>
  Мне не увидеться с тобой,<br>
  Прощай, Маруся, ламповая,<br>
  И ты, товарищ стволовой.<br>
  <br>
  Я был отважным коногоном,<br>
  Родная маменька моя,<br>
  Меня убило в темной шахте,<br>
  А ты осталася одна».<br>
  <br>
  <em>Русские народные песни. / Сост. и вводн. тексты В. В. Варгановой. 
  – М.: Правда, 1988 (перепечатано из сб.: Песни русских рабочих (XVIII - начало 
  XX века). М.-Л., 1962, тексты подготовил А. И. Нутрихин).</em><br>
 <br>
  <br>
  <em>Забуриться</em> - сойти с рельс. <em>Коногон (загонщик) </em>- погонщик 
  лошадей в шахте при конной откатке вагонеток. <em>Пары</em> - деревянные столбы, 
  служащие креплениями в шахтах. <em>Клеть</em> - кабина для подъема и спуска 
  шахтеров в шахту.<em> Ламповая </em>- а) помещение, где заправляли маслом лампы 
  шахтеров; б) работница, заправлявшая лампы шахтеров маслом.<br>
  <br>
  <br>
  Обычно песня начинается куплетом <em>&quot;Гудки тревожно загудели...&quot; 
  </em>(часто по этой строчке и называется).<br>
  <br>
  Старая дореволюционная шахтерская песня - из тех времен, когда на шахтах использовали 
  лошадей (кое-где так было до середины 20 века). Ее вариант прозвучал в фильме 
  Леонида Лукова &quot;Большая жизнь&quot; о шахтерах Донбасса 1930-х гг. (1939).  Там песню пел отрицательный герой-вредитель Макар Ляготин 
  (его играл Лаврентий Масоха), сидя в пивной и играя на гармони (3 куплета):<br>
  <br>
  <em>Гудки тревожно загудели,<br>
  Народ бежит густой толпой, <br>
  А молодого коногона <br>
  Несут с разбитой головой.<br>
  <br>
  - Прощай, Маруся плитовая,<br>
  И ты, братишка стволовой,<br>
  Тебя я больше не увижу,<br>
  Лежу с разбитой головой.<br>
  <br>
  - Ах, то был ярый коногонщик,<br>
  Я ухажерочка твоя,<br>
  Тебя убило здесь на шахте,<br>
  А я осталася одна.<br>
  <br>
  - Прощай, Маруся плитовая...<br>
  </em> <br>
  Фильм был лидером кинопроката 1940 года, и, очевидно, именно из него мелодия 
  шагнула в фольклор Великой Отечественной войны. Самая известная фронтовая переработка 
  - &quot;<a href="../ww2/folk/gibeltankista.php">По полю танки грохотали</a>&quot;. 
  На фронт ушла и другая песня, специально написанная для &quot;Большой жизни&quot; 
  - <a href="file:///C|/users/oceania/&#1056;&#1072;&#1073;&#1086;&#1095;&#1080;&#1081;%20&#1089;&#1090;&#1086;&#1083;/drugije/kurgany.htm">&quot;Спят курганы темные&quot;</a> (музыка 
  Никиты Богословского, слова Бориса Ласкина). В 1946 году Луков снял продолжение  (считается 2-й серией), посвященное послевоенному возрождению 
  Донбасса. Макар Ляготин стал положительным героем. По сюжету фильма в годы 
  войны он, внедрившись в полицию, работал на советское подполье и был награжден 
  медалью. В новой серии Ляготин опять пел эту песню (полтора куплета, причем 
  Маруся стала <em>&quot;ламповая&quot;</em>). Вообще, вторая серия фильма лучше 
  первой - и естественней, и человечней. Первая в этом отношении ужасна. Создателям 
  фильма подобной вольности не простили. 4 сентября 1946 года постановлением ЦК 
  ВКП(б) «О кинофильме &quot;Большая жизнь&quot;» фильм запрещен, так как &quot;введенные 
  в фильм песни (композитор Н. Богословский, авторы текстов песен А. Фатьянов 
  и В. Агатов) проникнуты кабацкой меланхолией&quot;. Еще ранее вышли постановления 
  «О журналах &quot;Звезда&quot; и &quot;Ленинград&quot;» (14 августа) и «О репертуаре 
  драматических театров и мерах по его улучшению». С этих документов началась 
  массовая чистка репертуара советского искусства, продолжавшаяся до 1953 года. 
  Фильм вновь вышел на экраны в 1963 году.<br>
  <br>
  <em>Плитовая</em> - рабочая на поворотной плите в шахте, она вручную разворачивала 
  вагонетки перед подъёмником. Эту работу выполняли женщины. А в годы Великой 
  Отечественной войны женщинам пришлось выполнять любые шахтерские работы - мужчины 
  ушли на фронт. Лишь в начале 1960-х женщин запретили брать на работу в шахтах, 
  но ранее принятые оставались до тех пор, пока не выработали подземный стаж. 
  Официально же СССР был участником конвенции о запрете женского труда под землей, 
  потому факт существования в стране &quot;шахтарок&quot; (или &quot;шахтарочек&quot;) 
  сильно не афишировался. В 2007 году в Донецке в честь шахтарок поставили мемориальную плиту .<br>
