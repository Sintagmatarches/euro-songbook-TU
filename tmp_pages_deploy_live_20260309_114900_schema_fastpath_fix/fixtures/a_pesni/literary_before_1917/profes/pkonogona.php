  <strong>5. Данила-коногон</strong><br>
  <br>
  Гудки тревожно прогудели,<br>
  Народ валит густой толпой.<br>
  А молодого коногона<br>
  Несут с разбитой головой.<br>
  <br>
  Зачем, Данила, торопился,<br>
  Зачем коня так быстро гнал?<br>
  Или десятника боялся,<br>
  Или в контору задолжал?<br>
  <br>
  - Десятника я не боялся,<br>
  В контору я не задолжал.<br>
  Меня товарищи просили,<br>
  Чтоб я коня быстрее гнал.<br>
  <br>
  Ох, шахта, шахта, ты - могила,<br>
  Зачем сгубила ты меня?<br>
  Прощайте все мои родные,<br>
  Вас не увижу больше я.<br>
  <br>
  В углу заплачет мать-старушка,<br>
  Слезу рукой смахнет отец.<br>
  И дорогая не узнает,<br>
  Каков мальчишки был конец.<br>
  <br>
  Прощай, Маруся ламповая,<br>
  Ты мой товарищ стволовой,<br>
  Тебя я больше не увижу -<br>
  Лежу с разбитой головой.<br>
  <br>
  Гудки тревожно загудели,<br>
  Народ идет густой толпой.<br>
  А молодого коногона<br>
  Несут с разбитой головой.<br>
