        <span class="text"> <span class="header"><strong>ЛЬВЫ СИЛОЙ 
        </strong> <br>
        </span> Сокольский марш<br>
        <br>
        Перевод с чешского М.В. Кондаковой<br>
        <br>
Львы силой, взлетом соколы,<br>
Мы дружно все вперед пойдем.<br>
Для нашей милой родины<br>
Всю жизнь мы в жертву принесем.<br>
Наш путь далек и труден он,<br>
Но не страшна для нас борьба;<br>
Вперед за славу родины,<br>
Смелее, братья сокола!<br>
<br>
Мы в высь, с душою смелою,<br>
Направим мощный свой полет,<br>
С горячей, твердой верою,<br>
Что нас в борьбе победа ждет!<br>
Пусть гордо знамя сокола<br>
Взовьется над родной землей,<br>
И славы солнце яркое<br>
Зажжется с новою зарей!<br>
		<br>
		<em>Газета «Армия и народ», Уфа, издание  Бюро печати Камской группы Народной армии. №87, воскресенье, 22 декабря 1918 г. С. 1.</em><br>
        <br>
        <br>
Текст содержится в очерке «В эти трудные дни...», подписанном  «Стрелок Камской группы И.Т.П.», дата под очерком - 18 декабря 1918 г. Автор-народоармеец в декабре 1918 г., в разгар Гражданской войны, приезжает с фронта лечиться в Уфу. Из-за безденежья он вынужден далеко идти пешком по холоду и вспоминает эту песню, под которую с коллегами по секции сокольской гимнастики в 1914 г. уходил на Первую мировую войну. Спортивно-националистическое движение «Сокол» было основано в 1862 г. Праге выпускником Карлового унверситета Мирославом Тыршем. Оно быстро завоевало популярность в славянских странах, включая Россию, где, однако, организационно оформилось только накануне Первой мировой войны: в 1910 г. создан Союз русского сокольства, в 1911 г. прошел его съезд. Участники первого чешского кружка «Сокол» занимались строевой подготовкой, гимнастикой, тяжелой атлетикой и фехтованием, но движение в целом ассоциируется прежде всего с гимнастикой.   <br><br>
Походную песню «Львы силой», ставшую гимном «соколов», написал Франтишек Йозеф Пельц, окончательный вид придал композитор и дирижер Франтишек Кмох. На русский язык гимн переведен участницей движения М.В. Кондаковой. Известно издание нот и текста русской версии марша на недатированной почтовой открытке, вышедшей, очевидно, между 1904 г., когда Всемирный почтовый союз ввел стандарт, использованный в этой открытке, и 1914 г. - началом Первой мировой войны, в ходе которой Чехия, как часть Австро-Венгрии, стала противником России (многие «соколы», как стороннники панславизма, сдались в русский плен и вступили в Чехо-словацкий легион). Репринт открытки см.: Кружнов Ю.Н. Нотная открытка в фондах Российской национальной библиотеки // Нотные издания в музыкальной жизни России. Вып. 4. СПб: Российская национальная библиотека, 2013.  С. 409-442: 418-119. Оригинал хранится в Отделе нотных изданий и музыкальных звукозаписей Российской национальной библиотеки.<br><br>
См. также польские сокольские марши - <a href="../polsk/marszsokolow-b.htm">Marsz Sokołów</a> на слова Любомира Бенедиктовича и более известный <a href="../polsk/marszsokolow.htm">Marsz Sokołów</a> на слова Яна Лама. <br>
<br>   
        <br>        
        <strong>Фрагмент очерка «В эти трудные дни...»:</strong><br>
        <br> Дожидаться очереди у кассы театра долго и противно, к тому же тело ныло от усталости, ноги подкашивались. Я рванулся к выходу и на улице свободно вздохнул свежим морозным воздухом. Идти до больницы порядочно, а извозчик возьмет какой-нибудь купон в 12 р. 50 коп. 13 р. 75 коп. а то и всю керенку в 20 руб. Мой министр финансов всеми фибрами души запротестовал на счет извозчика и повелительно скомандовал: «В больницу ша-гом марш!» Шагать было не весело. Дорогой вспомнил 18 июля 1914 года, когда вспыхнула война Сербии с Австрией. В это время мы занимались сокольской гимнастикой в саду мужской гимназии под руководством всеми уважаемого чеха Ярослава Осиповича Янчак. Почти всем курсистам мужчинам пришлось вступить в ряды армии, т. к. у нас в России была объявлена мобилизация. На прощанье с Я.О. Мы — курсанты спели в последний раз сокольский марш «Львы силой», который бодрил меня дорогой и 18 дек. 1918 г.<br>
<br>
          Как почти 4 ½ года тому назад, так и теперь в эти трудные дни для Родины оставили все свои личные дела и «смело мы в бой пойдем за Русь Святую и, как один, умрем за дорогую!».<br>
<br>
