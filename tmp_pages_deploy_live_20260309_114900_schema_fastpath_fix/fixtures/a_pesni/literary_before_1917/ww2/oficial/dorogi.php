Александрова. По другой - Иван Шмелев с Ансамблем песни и пляски войск НКВД на театрализованной программе «Весна победная», поставленной 7 ноября 1945 года режиссером оркестра Сергеем Юткевичем, а сама песня специально написана для этой постановки. Потом в течение месяца доработана и вошла в репертуар других ансамблей. <br>
