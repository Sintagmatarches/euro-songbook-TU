  приняли с таким вниманием, ее так напряженно слушали, что певцы, не уходя с 
  площади, пели ее раз за разом для все новых бойцов перед их посадкой в эшелоны.<br>
  <br>
  И уже в следующее свое посещение ДЗЗ [Дома звукозаписи], 28 июня, Александровский 
  ансамбль впервые спел «Священную войну» для граммофонного диска. В самые сжатые 
  сроки оригинал прошел всю техническую цепочку, и уже 4 июля два прессовочных 
  станка выдали пробные пластинки. Начиная со следующего утра, ежедневно, в течение 
  всех военных лет песня «Священная война» открывала передачи Всесоюзного радио. 
  Коробка с заигранными и новыми пластинками с нею долго, как реликвия, сохранялась 
  в радийной граммотеке&quot;.</em><br>
