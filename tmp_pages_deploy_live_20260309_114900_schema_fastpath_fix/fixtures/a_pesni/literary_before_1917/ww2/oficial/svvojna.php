 <br>
  <br>
  Стихотворение написано Василием Лебедевым-Кумачом в ночь с 22 на 23 июня 1941 
  года. 24 июня оно было опубликовано в газетах &quot;Красная звезда&quot; и 
  &quot;Известия&quot;. В тот же день руководитель ансамбля военной песни Александр 
  Александров написал музыку. 28 июня песня записана его ансамблем на грампластинку, 
  а 30 июня издана с нотами. Она стала лейтмотивом Великой Отечественной войны 
  - ежедневно &quot;Священная война&quot; открывала начинался радиоэфир.<br>
  <br> Куплеты 2, 6 и 7 в современных сборниках 
  обычно не печатаются (например, см: Русские советские песни (1917-1977). Сост. 
  Н. Крюков и Я. Шведов. М., «Худож. лит.», 1977). Возможно, эти куплеты просто 
  не прижились - текст оказался слишком длинный, или не влезли на пластинку (сторона 
  граммофонной пластинки вмещала 3 минуты). После публикации стихотворния музыку 
  к нему написали также М. Блантер, Н. Чаплыгин, М. Полонский и Е. Путилов, но 
  в оборот вошла мелодия Александрова. <br>
  <br>
  Данные о первом публичном исполнении песни расходятся. Официальная советская 
  версия, устоявшаяся во второй половине 20 века гласит, что песня впервые была исполнена 
  ансамблем Александрова на Белорусском вокзале 26 или 27 июня 1941 года перед 
  отъезжающими на фронт частями. Этой версии придерживаются и многие современные авторы, например, историк советской звукозаписи Глеб Скороходов<br>
  <br>
  
  С другой стороны, исследователь военной песни Юрий Бирюков утверждает, что &quot;Священная война&quot; сразу попала 
  под неофициальный запрет за пессимистический настрой - в первые дни войны поощрялись 
  шапкозакидательские настроения. И на вокзале в июне оркестром Александрова она, по словам Бирюкова,
  не исполнялась. Запрет продолжался, пока ситуация на московском направлении 
  не стала критической - 13 октября 1941 пала Калуга, 14 октября Ржев и Калинин. 
  Тогда 15 октября 1941 года песню впервые поставили по всесоюзному радио, и с 
  тех пор она звучала по радио каждый день. А сцена исполнения песни в июне на 
  Белорусском вокзале впервые появилась в романе Константина Федина &quot;Костер&quot; 
  (1961-1965) и была плодом литературного вымысла автора. В дальнейшем этот вымысел 
  приняли за документальный факт. (См. ниже: В. Кожинов &quot;Поэзия военных лет&quot;, 
  1999 г.).<br>
  <br>
  Появившаяся в Перестройку (не позднее 1990 года) версия о том, что у &quot;Священной 
  войны&quot; существовал прообраз времен первой мировой войны, якобы написанный 
  в 1916 году учителем из Рыбинска Александром Боде (версию высказал в 1990 на 
  радио &quot;Свобода&quot; журналист Андрей Мальгин со ссылкой на дочь Боде Зинаиду 
  Колесникову, вскоре умершую), не нашла документального подтверждения: не предъявлен ни черновик Боде, ни его письмо с текстом, якобы направленное Лебедеву-Кумачу. См.: С. Макин. <a href="a-svvojna.php">Священная 
  война: момент истины</a> (2002). Саму версию о первоисточнике Боде см., например: 
  А. Азаренков. <a href="../../drugije/a-vorov.php">Ворованная песня</a> // Военно-исторический 
  журнал &quot;Рейтар, №2 (26), 2006.<br>
  <br>
  В первые недели войны официальными поэтами и композиторами было создано более 
  200 заказных патриотических военных песен, но почти ни одна из них не прижилась; 
  &quot;Священная война&quot; - исключение, но это, по сути, гражданская песня.<br>
  <br>
  В окопах пели, наоборот, &quot;антивоенные&quot; песни - о возвращении, о доме, 
  о женах: <a href="vzemlanke.htm">&quot;В землянке&quot;</a>, <a href="zdimenja.php">&quot;Жди 
  меня&quot;</a>, <a href="sinplatotchek.htm">&quot;Синий платочек&quot;</a>, 
  <a href="tyodessit.htm">&quot;Ты одессит, Мишка!&quot;</a>... <br>
  <br>
  В начале 2000-х песня пользовалась популярностью в некоторых кругах молодежного 
  радикального антифашистского движения (это когда бьют морду наци-скинам и т. 
  п.). У анархистов популярностью не пользуется, впрочем, как и вообще песни Великой 
  Отечественной войны.<br>
