  прифронтовом&quot; (&quot;С берез неслышен, невесом...&quot;), &quot;Огонек&quot; 
  (&quot;На позицию девушка провожала бойца..,&quot;) и &quot;Враги сожгли родную 
  хату...&quot; Михаила Исаковского, &quot;Соловьи&quot; (&quot;Соловьи, соловьи, 
  не тревожьте солдат...&quot;), &quot;На солнечной поляночке...&quot; и &quot;Давно 
  мы дома не были&quot; (&quot;Горит свечи огарочек.,.&quot;) Алексея Фатьянова, 
  &quot;В землянке&quot; (&quot;Бьется в тесной печурке огонь...&quot;) Алексея 
  Суркова, &quot;Дороги&quot; (&quot;Эх, дороги, пыль да туман...&quot;) Льва 
  Ошанина, &quot;Случайный вальс&quot; (&quot;Ночь коротка, спят облака...&quot;) 
  Евгения Долматовского, &quot;Темная ночь&quot; Владимира Агатова (для которого 
  эта песня, по-видимому, была единственным творческим взлетом...). Слова этих 
  песен, конечно же, всецело порождены войной, но на первом плане в них - не война, 
  а тот мир, который она призвана спасти. <br>
  <br>
  Правда, есть еще одна также известная всем и тогда, и теперь песня, которая 
  имеет иной характер - &quot;Священная война&quot; (&quot;Вставай; страна огромная...&quot;) 
  Василия Лебедева-Кумача. Но во-первых, она - одна такая, а во-вторых, это, в 
  сущности, не песня, а военный гимн. Написанные в ночь с 22 на 23 июня (24 июня 
  текст был уже опубликован в газетах) слова этого гимна, надо прямо сказать, 
  не очень уж выдерживают художественные критерии; у Лебедева-Кумача есть намного 
  более &quot;удачные&quot; слова песен, - скажем: <br>
  <br>
  <em>Я на подвиг тебя провожала, - <br>
  Над страною гремела гроза. <br>
  Я тебя провожала <br>
  И слезы сдержала <br>
  И были сухими глаза... </em><br>
  <br>
  Но в &quot;Священной войне&quot; все же имеются своего рода опорные строки, 
  которые находили и находят мощный отзвук в душах людей: <br>
  <br>
  <em>...Вставай на смертный бой . <br>
  ...Идет война народная, Священная война... </em><br>
  <br>
  И о противнике: <br>
  <br>
  <em>Как два различных полюса <br>
  Во всем враждебны мы... </em><br>
  <br>
  И призыв, близкий по смыслу другим песням: <br>
  <br>
  <em>...Пойдем ломить всей силою, <br>
  Всем сердцем, всей душой <br>
  За землю нашу милую...</em><br>
  <br>
  На эти строки, в свою очередь, оперлась героико-трагическая мелодика композитора 
  А. В. Александрова, и родился покоряющий всех гимн. Надо иметь в виду, что гимн 
  этот люди, в общем, не столько пели, сколько слушали, подпевая ему &quot;в душе&quot;, 
  и едва ли помнили его слова в целом, - только &quot;опорные&quot;. <br>
  <br>
  Как и многие обладающие высокой значимостью явления, &quot;Священная война&quot; 
  обросла легендами - и позитивными, и негативными. С одной стороны, постоянно 
  повторяли, что прославленный Ансамбль песни и пляски Красной Армии пел ее для 
  отправлявшихся на фронт войск на Белорусском вокзале уже с 27 июня 1941 года. 
  Между тем скрупулезный исследователь знаменитых песен Юрий Бирюков по документам 
  установил, что вплоть до 15 октября 1941 года &quot;Священная война&quot; была, 
  как говорится, в опале, ибо некие предержащие власти считали, что она чрезмерно 
  трагична, с первых строк обещает &quot;смертный бой&quot;, а не близкое торжество 
  победы... И только с 15 октября - после того, как враг захватил (13-го) Калугу 
  и (14-го) Ржев и Тверь-Калинин, - &quot;Священная война&quot; стала ежедневно 
  звучать по всесоюзному радио. Сцену же, якобы имевшую место в первые дни войны 
  на Белорусском вокзале, создал художественным воображением Константин Федин 
  в своем романе &quot;Костер&quot; (1961-1965), и отсюда сцена эта была перенесена 
  во многие будто бы документальные сочинения. <br>
  <br>
  С другой стороны, с 1990 года начали публиковаться совершенно безосновательные 
  выдумки о том, будто бы &quot;Священная война&quot; была написана еще в 1916 
  году неким обрусевшим немцем. Но это - один из характерных образчиков той кампании 
  по дискредитации нашей великой Победы, которая столь широко развернулась с конца 
  1980-х годов: вот, мол, &quot;главная&quot; песня сочинена за четверть века 
  до 1941-го, да еще и немцем... Юрий Бирюков, анализируя сохранившуюся в Российском 
  государственном архиве литературы и искусства черновую рукопись Лебедева-Кумача, 
  в которой запечатлелись несколько последовательных вариантов многих строк песни, 
  неоспоримо доказал, что текст принадлежит её &quot;официальному&quot; автору. 
  <br>
  <br>
  Важно сказать еще, что нынешние попытки дискредитации прославленной песня лишний 
  раз свидетельствуют о той первостепенной роли, которую сыграла песня (и поэзия 
  вообще) в деле Победы! Ибо оказывается, что для &quot;очернения&quot; великой 
  войны необходимо &quot;обличить&quot; и ее песню... <br>
  <br>
  Сам Г. К. Жуков на вопрос о наиболее ценимых им песнях войны ответил так; &quot;&quot;Вставай, 
  страна огромная...&quot;, &quot;Дороги&quot;, &quot;Соловьи&quot;.., Это бессмертные 
  песни... Потому что в них отразилась большая душа парода&quot;, и высказал уверенность, 
  что его мнение не расходится с мнением &quot;многих людей&quot;*. И в самом 
  деле к маршалу, конечно же, присоединились бы миллионы людей, хотя и добавив, 
  может быть, в его краткий перечень еще и &quot;В лесу прифронтовом&quot;, &quot;Темную 
  ночь&quot;, &quot;В землянке&quot; и т д. <br>
  <br>
  Но обратим еще раз внимание на то, что собственно &quot;боевая&quot; песня - 
  &quot;Священная война&quot; - только одна из вошедших в &quot;золотой фонд&quot;; 
  остальные, как говорится, &quot;чисто лирические&quot;. И вроде бы даже трудно 
  совместить &quot;ярость&quot; этого гимна с просьбой к соловьям &quot;не тревожить 
  солдат&quot;, хотя маршал Жуков поставил и то и другое в один ряд. <br>
  <br>
