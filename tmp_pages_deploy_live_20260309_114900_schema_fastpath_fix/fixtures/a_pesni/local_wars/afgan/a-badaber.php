        <h1>БЕЛЫЕ ПЯТНА АФГАНСКОЙ ВОЙНЫ. БАДАБЕРСКОЕ ВОССТАНИЕ</h1> 
     
        <span class="i">Тюменские известия. 2011. <a href="http://t-i.ru/article/18616/" target="_blank">№72 
(5280)</a>. 29 апр.<br></span>
       <br>
        <br>
        <img src="a-badaber.jpg" alt="Григорий Григорьев, ветеран афганской войны, подполковник ВДВ, председатель 
        общественной организации «Союз десантников»" width="100%"><br>
        <em>Григорий Григорьев, ветеран афганской войны, подполковник ВДВ, председатель 
        общественной организации «Союз десантников»</em><br>
        <br>
        <br>
        <strong>15 февраля 2011 года мы отметили 22-ю годовщину вывода советских 
        войск из Афганистана.</strong><br>
        <br>
        Выступая перед школьниками, студентами в преддверии этой даты, задаешь 
        один вопрос: «Знаете ли вы героев Гражданской войныы 1918-1921 годов?» 
        Ребята называют известных героев — Чапаева, Буденного, Колчака. «А героев 
        Великой Отечественной войны?» И здесь — узнаваемые личности: маршал Г. 
        Жуков, герой А. Матросов, разведчик Н. Кузнецов. Но уточняешь: «А знаете 
        ли вы героев афганской войны?» — и видишь удивленно открытые ребячьи глаза. 
        В них немой вопрос: что это за война?<br>
        <br>
        Вот так незаметно из непопулярной, непонятной с точки зрения политических 
        целей афганская война переходит в разряд неизвестной. А ведь длилась она 
        ни много ни мало 9 лет 1 месяц и 19 дней. По официальным данным, через 
        горнило этой войны прошло более 620 тысяч солдат и офицеров, 15 тысяч 
        погибло, свыше 50 тысяч получило ранения. После вывода войск около 300 
        человек числилось в плену и пропавшими без вести. Но даже попав в плен, 
        советские солдаты и офицеры не сломались и сохранили веру в себя и свою 
        Родину. Один из эпизодов афганской войны в апреле 1985 г. показал их героизм 
        и мужество.<br>
        <br>
        В 1983-1985 гг. в небольшом кишлаке Бадабер под городом Пешавар на территории 
        Пакистана в двадцати километрах от границы с Демократической Республикой 
        Афганистан находился лагерь афганских беженцев. При нем был организован 
        центр подготовки боевиков. Под руководством военных инструкторов из США, 
        Пакистана, Китая, Египта там проходили обучение будущие моджахеды, планировавшие 
        вернуться в Афганистан для продолжения сопротивления советским войскам.<br>
        <br>
        Сам учебный центр принадлежал партии «Исламское общество Афганистана» 
        (лидер — Бурхануддин Раббани), одной из самых влиятельных и крупных оппозиционных 
        групп. Помимо центра и складов с оружием там располагалось три тюрьмы. 
        В них привозили военнослужащих вооруженных сил ДРА и «шурави» — советских 
        военнопленных, захваченных в Афганистане. По разным данным, там находилось 
        40 пленных афганцев и около 20 советских военнопленных.<br>
        <br>
        Вот выдержка из аналитической записки разведотдела 40-й армии, с которой 
        лишь недавно сняли гриф секретности:<br>
        <br>
        «26 апреля 1985 г. во время вечернего намаза группа советских военнослужащих 
        тюрьмы Бадабер разоружила часовых у артиллерийских складов и, взломав 
        замки в арсенале, вооружилась спаренной зенитной установкой, крупнокалиберным 
        пулеметом, минометом, гранатометами. Советские воины заняли ключевые точки 
        крепости-тюрьмы: несколько угловых башен и здание арсенала.<br>
        <br>
        По тревоге был поднят весь личный состав базы — около 3000 человек во 
        главе с инструкторами. Они пытались штурмом вернуть контроль над крепостью, 
        но были встречены шквальным огнем и, понеся большие потери, отступили. 
        Лидер ИОА Б. Раббани попытался приказать мятежникам сдаться, но получил 
        ответные требования — вызвать представителей посольств СССР, ДРА, Красного 
        Креста, ООН.<br>
        <br>
        Место боя было блокировано тройным кольцом окружения, состоящим из душманов 
        и военнослужащих 11-го армейского корпуса пакистанской армии, а также 
        бронетехникой и артиллерией. В воздухе барражировали боевые вертолеты 
        ВВС Пакистана. Жесточайшее боестолкновение продолжалось всю ночь. Штурм 
        следовал за штурмом. Силы восставших таяли, но повстанцы не сдавались. 
        27 апреля по приказу Раббани на прямую наводку вывели тяжелую артиллерию. 
        И лишь после попадания снаряда в склад боеприпасов произошел мощный взрыв, 
        от которого все восставшие погибли».<br>
        <br>
        В восстании участвовали и погибли, по разным оценкам, от 12 до 15 советских 
        военно-служащих. Против них действовали моджахеды Раббани и 11-й армейский 
        корпус Пакистана, потери которых составили около 100 моджахедов, 90 военнослужащих 
        пакистанских войск, 6 американских инструкторов, 3 установки «Град», 40 
        единиц боевой техники, орудий и минометов. Сгорела канцелярия тюрьмы, 
        а с ней и списки пленных. В результате боя учебный центр и тюрьма Бадабер 
        были уничтожены.<br>
        <br>
        Учитывая итоги восстания, один из лидеров афганской оппозиции — руководитель 
        Исламской партии Афганистана (ИПА) Г.Хекматияр издал приказ, в котором 
        потребовал усилить охрану пленных «шурави». В приказе было дано указание 
        «впредь русских в плен не брать, в Пакистан не переправлять, а уничтожать 
        на месте захвата».<br>
        <br>
        Случившееся в Бадабере пакистанские власти и руководство моджахедов пытались 
        скрыть. Радиостанция «Голос Америки» и другие западные СМИ представили 
        все произошедшее как случайный взрыв в армейском арсенале, где есть погибшие 
        и пострадавшие. И в нашей стране эта информация нигде, кроме засекреченных 
        дипломатических заявлений, не распространялась. В СССР долго ничего не 
        знали о героях Бадабера. Хотя слухи о восстании в какой-то крепости ходили 
        с мая 1985 г. И лишь в 1990 г. армейская газета «Красная звезда» впервые 
        рассказала об этом подвиге. Были названы фамилии восставших героев (правда, 
        не всех) и восстановлена хроника событий.<br>
        <br>
        Я сам проходил службу в Афганистане и знаю, как попадали в плен наши бойцы. 
        Большинство из них стыдилось этого позора, пытаясь скрыть свое звание, 
        должность, фамилию, личные данные. А в плену вместо имен и фамилий им 
        присваивали мусульманские имена и принуждали носить чужую одежду. Для 
        унижения человеческого достоинства некоторых пленников клеймили, заковывали 
        в цепи, морили голодом, подсыпали в скудную пищу дешевые наркотики.<br>
        <br>
        Среди пропавших без вести в афганской войне есть и наши земляки. Среди 
        них Николай Васильев, уроженец Заводоуковского района, призванный в Вооруженные 
        силы в апреле 1983 г. Пропал без вести 15 ноября 1983 г. в провинции Герат.<br>
        <br>
        В числе известных на сегодняшний день героев Бадаберского восстания есть 
        и сержант Васильев П.П. (по другим данным — Васильев В.П.). Но я уже писал, 
        что, попав в плен, наши военнослужащие, не имея документов на руках, могли 
        называться другими именами. А судьба Николая Васильева до сих пор не известна.<br>
        <br>
        Почему я пишу об этом? Потому, что любая война — это кровь, пот, страдания, 
        потери, но в то же время это и надежда, вера, подвиг. Произошедшее в Бадабере 
        не было ни самым драматическим, ни самым кровавым эпизодом афганской войны. 
        Случались вещи и похуже. Но хотелось бы, чтобы белых пятен любой войны 
        становилось меньше. Поиск героев продолжается.<br>

