	<h1>АФГАНИСТАН 
</h1>
<br>
Пришёл приказ — <br>
И по тревоге мы встаём. <br>
Взяв автомат, <br>
Садимся молча в самолёт. <br>
В рассветный час, <br>
Когда земля ещё спала, <br>
В Афганистан <br>
Приказ нам воля занесла. <br>
<br>
Афганистан, <br>
Красивый горный дикий край, <br>
Приказ простой: <br>
Иди вперёд и умирай! <br>
Но как же так?! <br>
Ведь на земле весна давно, <br>
И сердце лишь <br>
Тоскует, горести полно. <br>
<br>
Афганистан — <br>
Грохочет где-то пулемёт. <br>
Афганистан... <br>
Погиб вчера мальчишек взвод. <br>
Их командир <br>
С свинцом в груди на землю пал. <br>
«Свободным будь», — <br>
Он перед смертью прошептал. <br>
<br>
Рыдает мать, <br>
И словно тень стоит отец <br>
Ведь ихний сын, <br>
Ведь он для них ещё юнец. <br>
Всё тот же он, <br>
Но с сединою на висках. <br>
Такая боль <br>
В больших, как омуты, глазах. <br>
