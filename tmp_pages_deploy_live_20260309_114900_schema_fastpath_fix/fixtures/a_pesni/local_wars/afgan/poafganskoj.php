<h1>ПО 
АФГАНСКОЙ, РАСКАЛЕННОЙ СОЛНЦЕМ ЗЕМЛЕ…</h1>
<br>
По афганской, раскаленной солнцем земле <br>
Поднимается пыль среди горных вершин. <br>
Ты сидишь с автоматом в руках на броне, <br>
И все дальше уходит колонна машин. <br>
Компас, карта и спальный мешок за спиной – <br>
Все, что нужно опять на пути и в бою. <br>
И опять рядом слышен мне твой позывной — <br>
Подаешь ты пилотам команду свою. <br>
<br>
        <em>Припев:</em> <br>
<br>
Пять, квадрат «по улитке», семерка. <br>
Там душманы засели в горах. <br>
Вновь заходят по парам «восьмерки», <br>
Знай, что дело в надежных руках. <br>
<br>
Не хватает воды, и паек не богат, <br>
И от пота к спине прилипает хэбэ, <br>
От жары не спастись, здесь она — пятьдесят, <br>
Вот такая работа досталась тебе... <br>
Мы встречаемся редко на несколько дней, <br>
Мы здесь сами встаем, без команды «Подъем!», <br>
Здесь же, рядом, друзья среди скал на броне — <br>
Только их голоса мы в эфире найдем. <br>
<br>
        <em>Припев. </em><br>
<br>
Среди горных вершин вознеслись в облака. <br>
Стрелка компаса к северу, к дому зовет. <br>
Только путь твой на юг пролегает пока, <br>
Но ты знаешь, что дома любимая ждет. <br>
По афганской, раскаленной солнцем земле <br>
После боя вернется колонна домой, <br>
И не хочется думать об этой войне... <br>
Только завтра опять начинается бой. <br>
<br>
        <em>Припев. </em><br>
<br>
Вот закончится все, смолкнет грома раскат, <br>
Ты вернешься в родные края под Москвой, <br>
Но тебе никогда не забыть тех ребят, <br>
Что погибли, шагая той горной тропой.<br>
<br>
        <em>Припев: </em><br>
<br>
Пять, квадрат «по улитке», семерка. <br>
Там душманы засели в горах. <br>
Вновь заходят по парам «восьмерки», <br>
Знай, что дело в надежных руках. <br>
