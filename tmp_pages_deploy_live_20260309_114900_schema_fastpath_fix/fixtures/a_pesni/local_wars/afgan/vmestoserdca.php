<h1>ВМЕСТО СЕРДЦА КАМЕННЫЙ ПРОТЕЗ</h1>
<br>
Опершись на капот чужой машины<br>
И от боли закусив губу,<br>
Он смотрел, как чинно те выходят,<br>
Что с трибуны только что орали ему:<br>
<br>
<em>Припев:</em><br>
<br>
- Да ты ж герой! Тебе поем мы славу<br>
И в беде тебя не бросим никогда!<br>
Ты за нас сражался там, в Афганистане.<br>
Здесь мы… ну… повоюем за тебя!<br>
<br>
Вот они выходят из подъездов,<br>
Размещаясь в поданных авто,<br>
Обсуждают юности проблемы,<br>
Но никто не смотрит на него.<br>
<br>
<em>Припев.</em><br>
<br>
Вот бы кто из тех вояк трибунных<br>
Предложил бы просто рядом сесть!..<br>
Только вместо помощи реальной<br>
Снова слышим пламенную лесть.<br>
<br>
<em>Припев.</em><br>
<br>
Нет ноги, но бьется честно сердце.<br>
Есть дорога, а нога… Попробуем и без.<br>
А у вас, бойцы за справедливость,<br>
Вместо сердца каменный протез.<br>
<br>
<em>Припев:</em><br>
<br>

- Да ты ж герой! Тебе поем мы славу<br>
И в беде тебя не бросим никогда!<br>
Ты за нас сражался там, в Афганистане.<br>
Здесь мы… ну… повоюем,<br>
ну, конечно, повоюем,<br>
Но только с трибуны повоюем за тебя!<br>
