	<h1>ВПЕРЁД, РОССИЯ!<br>
Частушки </h1>
<br>
— Ты, браток, руки лишился <br>
На Кавказе, на войне? <br>
— Нет, в военкомат тащили, <br>
Оторвали руку мне! <br>
<br>
День Победы до сих пор <br>
Празднует весь шар земной, <br>
Только русских почему-то <br>
Снова гонят на убой! <br>
<br>
Отдают меня в солдаты — <br>
Молодца и вольницу, — <br>
Запираю свою герлу <br>
На два года в горницу. <br>
<br>
— Милый мой, пойдём домой, <br>
Пойдём, моя кровинушка! <br>
Тебя угонят во Чечню, <br>
Останусь сиротинушка! <br>
<br>
Я любила старшину <br>
За порядок, тишину. <br>
А теперь — десантника <br>
За силу и романтику! <br>
<br>
Знаю я тебя, военный, <br>
Ты военный непростой! <br>
Ты не севере женатый, <br>
А на юге холостой! <br>
<br>
Лейтенанта полюбила — <br>
Оказался старшина. <br>
Не успела познакомиться — <br>
Приехала жена... <br>
<br>
Полюбила лейтенанта – <br>
Оказался рядовой. <br>
Он довёл меня до дому <br>
И скомандовал: «Домой!» <br>
<br>
Всех в пехоту да в пехоту, <br>
Меня, мальчика, — во флот. <br>
Прямо в Баренцево море, <br>
На подводный пароход! <br>
<br>
Я ли, я ли не люблю <br>
Молодую шельму?! <br>
Уведу за гарнизон, <br>
Укутаю шинелью! <br>
<br>
Поздно, поздно, мама, плакать, <br>
Поздно плакать обо мне! <br>
Ведь не всех же убивают, <br>
Дорогая, на Чечне... <br>
<br>
...Хватит музыку играть, <br>
Довольно выфигуривать! <br>
Ваши пальчики устали — <br>
Можете закуривать... <br>
