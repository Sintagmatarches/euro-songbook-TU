  В родную деревню иду.<br>
<br>Блеснула знакомая 
  хата,<br>
  Родная корова мычит,<br>
  Играют в горелки ребята,<br>
  И мать у колодца стоит.<br>
<br>Я бросился с криком 
  к родимой<br>
  И к сердцу прильнул головой.<br>
  «Ах, здравствуй, сынок мой родимый,<br>
  Вернулся ты снова домой!»<br>
<br>Потом от меня отшатнулась,<br>
  В лице побледнела слегка.<br>
  И ласково, тихо спросила:<br>
  «А где же нога-то, нога?»<br>
<br>«Ах, маменька, 
  тяжкую муку<br>
