Любила меня мать, обожала,<br>
Что я — ненаглядный сынок,<br>
В окопы меня провожала,<br>
Давала с собой узелок.<br>
<br>
Крестила дрожащей рукою,<br>
А слезы текли, как вода,<br>
На веки прощалась со мною,<br>
Прощалась со мной навсегда.<br>
<br>
- Прощай, мой сынок ненаглядный!<br>
Здоров и живехонек будь!<br>
Дерися с врагом беспощадно<br>
И мать на войне не забудь!<br>
<br>
Крестила дрожащей рукою,<br>
Смотрела сиротка в глаза,<br>
К щеке прижималась щекою,<br>
И тихо бежала слеза...<br>
<br>
- Одна я, одна, сиротинка!<br>
С хозяйством сама обойдусь!<br>
Одна я, как в поле былинка!<br>
Дождусь ли сыночка? дождусь?<br>
<br>
Простились мы с ней у вагона,<br>
Я долго крепился, молчал,<br>
Последнего матери стона<br>
За шумом колес не слыхал...<br>
<br>
Как воин, я бился с врагами,<br>
Героем у всех на виду...<br>
Украшен я всеми крестами<br>
В деревню понуро иду...<br>
<br>
Мелькнула знакомая хата,<br>
Родная корова мычит,<br>
Играют в горелки ребята,<br>
А мать у колодца стоит...<br>
<br>
Я бросился с криком к родимой<br>
И к сердцу припал головой...<br>
- Хороший... сыночек... любимый...<br>
Вернулся? Голубчик ты мой!<br>
<br>
Потом от меня отступила,<br>
В лице побледнела слегка<br>
И ласково-тихо спросила:<br>
- А где же рука-то? рука?<br>
<br>
Эх, матушка, страшную муку<br>
Стерпел я геройски вполне,<br>
И бедную левую руку<br>
Оставил врагу на войне...<br>
<br>
Что плакать? Я дрался со славой,<br>
Сумел я кресты заслужить,<br>
Рукою оставшейся правой<br>
Сумею я мать прокормить...<br>
<br>
Смотри-ка — здоровой рукою<br>
Беру я два полных ведра...<br>
Подумаешь — тяжесть какая...<br>
Эх, матушка, легче пера!..<br>
<br>
С сохою, с крестьянством, с работой,<br>
С тяжелой крестьянской нуждой<br>
Я справлюсь с большою охотой<br>
Здоровою правой рукой!..<br>
