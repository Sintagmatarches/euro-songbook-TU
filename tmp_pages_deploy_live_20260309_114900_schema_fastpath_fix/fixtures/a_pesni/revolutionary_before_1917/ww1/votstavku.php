	<br>
	В отставку с германского фронта,<br>
С далеких германских полей,<br>
Калека, солдат изнуренный,<br>
Плетется к деревне своей.<br>
Участвовал в битвах кровавых,<br>
Жестоких врагов принимал на штыки,<br>
Лишился он волею рока<br>
Кормилицы — правой руки.<br>
Героя труды «не пропали».<br>
В награду за храбрость и честь<br>
Геройскую грудь украшает<br>
На ленте серебряный крест.<br>
Идет он скорее увидеть<br>
Убогий, родимый свой дом.<br>
А сердце его замирает,<br>
 И ветер бушует кругом.<br>
Плетется герой утомленный -<br>
Отпущен совсем он домой.<br>
Но вот перед ним замелькали<br>
Огни деревеньки родной.<br>
И вот и родная избушка,<br>
За ней небольшой огород.<br>
В избе закопченные окна, -<br>
Наверно, никто не живет.<br>
Навстречу герою-калеке<br>
Никто из родных не бежит,<br>
Лишь только из хаты соседней<br>
Старуха одна говорит:<br>
- Эх, милый, несчастье случилось!<br>
За что вас господь наказал?!<br>
- За что? Почему? Что случилось? -<br>
Солдат ей с испугом сказал.<br>
- Жену твою некий приезжий<br>
В столицу с собою увез.<br>
А мать твоя бедная с горя<br>
В могилу пошла на погост.<br>
Сестра же твоя в лазарете.<br>
У койки дежурной стоит,<br>
И брат твой, лихой ополченец,<br>
В бою Марйуп<strong>о</strong>льском убит. -<br>
И слезы из глаз покатились,<br>
Не мог их герой удержать,<br>
Лишь только проклятье Николке<br>
Со злостью сумел он послать.<br>
