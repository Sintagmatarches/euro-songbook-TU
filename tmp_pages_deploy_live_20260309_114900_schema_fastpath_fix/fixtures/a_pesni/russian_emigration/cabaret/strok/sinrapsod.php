 <br>
  <br>
  Слоу-фокс (медленный фокстрот). Одна из песен, написанных Оскаром Строком и 
  Марком Марьяновским в начале 1930-х в Риге для начинающего песенную карьеру 
  Петра Лещенко. См. <a href="../marianovsky/barcelona.php">«Барселона»</a>, <a href="../marianovsky/dunia.php">«Блины»</a>, 
