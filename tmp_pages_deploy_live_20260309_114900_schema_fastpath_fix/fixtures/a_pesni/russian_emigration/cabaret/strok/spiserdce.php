  Из книги Дмитрия Драгилёва &quot;Лабиринты русского танго&quot; (Спб., Алетейя, 
  2008):<br>
  <br>
  &quot;«Спи, мое бедное сердце» опубликовано в сборнике «Русский джаз» не ранее 
  середины 1935 г. Соавтор текста – Е. Альтшулер. Отдаленные интонационные окрестности 
  и контексты (танго Эрнесто де Куртиса «Пой мне»; «Бедное сердце мамы» и др.) 
  позволяют сказать, что это танго вписывается в «итальянскую» линию Строка. <br>
  <br>
  Сезон 1933 года на Рижском взморье был особенно богат на события. Строк открывает 
  свое новое кафе, вслед за ним появляется знаменитая взморская достопримечательность 
  — дансинг «Лидо», в летних ресторанах работает и Лещенко. Двум англичанам понравилось 
  выступление певца. С этого апокрифического момента ведет отсчет его «колумбийская» 
  дискография: у Лещенко появляются ангажементы, связанные с записями на студиях 
  британской фирмы «Колумбия» в Вене и Бухаресте, участием в лондонских светских 
  раутах, музыкальным вещанием БиБиСи. У Строка очередные неприятности. Благодаря 
  доносу (будто бы в капелле Строка играет без разрешения гражданин Финляндии) 
  заведение посещает полицейский чин. Заказав ликер, он составляет протокол о 
  незаконной торговле спиртными напитками. Дело передается в суд и на Строка накладывается 
  штраф. Говорить о том, что танго «Спи, мое бедное сердце» как-то связано с кельнершей 
  кафе «Рандеву» — молодой миловидной Ольгой Ц. было бы романтической спекуляцией.<br>
  <br>
  Строк служил пианистом и капельмейстером не только в собственных кафе. В разные 
  годы он играет в рижских и взморских ресторанах «Парнас», «Фокстрот-Диле», «Рококо», 
  «Альгамбра», «Фемина», «Казино», «Лидо», «Маскотта», играет соло, со скрипачом 
  Я. Левинсоном или своей капеллой-квартетом, дает уроки художественного аккомпанемента, 
  проигрывает музыкальные новинки посетителям нотного магазина своего издательства: 
  по свидетельству современников, постоянные клиенты в большинстве своем — барышни, 
  интересующиеся модными песенками из-за рубежа (адреса издательства: ул. Бривибас 
  10, ул. Тиргоню 5, ул. Гоголя 13-5, ул. Элизабетес 85)&quot;. <br>
