	  <strong>Руслан Михалевский (Бельцы) </strong><br>
  <br>
<span class="header"><strong>ИЗА КРЕМЕР: БЕЛЬЧАНКА, ПОКОРИВШАЯ МИР</strong><br></span>
  <br>
  <br>
  В моих наушниках звучит старинная патефонная запись ностальгической песни о 
  &quot;воспоминаниях юных лет&quot;. Сквозь потрескивание под аккомпанемент рояля 
  и скрипки слышен красивый глубокий женский голос. Он принадлежит певице Изе 
  Кремер, покорившей своим талантом в 20-х годах прошлого века Европу, Северную 
  и Южную Америку. Ее имя звучало наравне с именами таких знаменитых певцов как 
  Александр Вертинский, Изабелла Юрьева, Вадим Козин. О ее успехе с завистью отзывался 
  Есенин, ее мечтал услышать Сталин, ей рукоплескали Черчилль и Рузвельт.<br>
  <br>
  Приятный для нас факт: великая певица 20-30-х годов Иза Кремер родилась в Бельцах.<br>
  <br>
  К сожалению, какие-либо подробности жизни Изы Яковлевны Кремер в Бельцах мне 
  найти не удалось. Родилась она в 1883 году (по другим данным в 1889, или в 1890 
  году). Блестящие музыкальные способности проявились у нее рано, и родители постарались 
  дать ей соответствующее образование. В 1912 году она отправилась в Милан (заметим, 
  что в то время во многих семьях еврейской интеллигенции родители стремились 
  дать детям самое лучшее образование, отправляя их на Запад). В течение двух 
  лет она училась пению у известного педагога Луиджи Ронци, затем выступала в 
  небольших театрах в Италии.<br>
  <br>
  После возвращения из Италии Иза Кремер приехала в Одессу. Ей предложили выступить 
  в Одесском оперном театре. Дебютировала она в опере Пуччини &quot;Богема&quot;. 
  Затем выступила в &quot;Травиате&quot;. На оперной сцене Иза пользовалась успехом, 
  но ее влекло что-то иное: вскоре она перешла в оперетту, где сразу завоевывала 
  колоссальное признание публики. Среди оперетт, в которых она участвовала, были 
  &quot;Нищий студент&quot;, &quot;Идеальная жена&quot;, &quot;Наконец один&quot;, 
  &quot;Польская кровь&quot;...<br>
  <br>
  В Одесса Иза вышла замуж за главного редактора газеты &quot;Одесские новости&quot; 
  Израиля Моисеевича Хейфеца. Благодаря ему, она вошла в круг местных литераторов.<br>
  <br>
  В 1915 году в Одессе состоялся большой концерт, на котором выступали видные 
  артисты. С одной сентиментальной песенкой выступила и Иза Кремер, заслужив бурю 
  аплодисментов. С той поры Иза выступала на эстраде, исполняя народные песни, 
  романсы, шуточные, интимные и лирические песенки, многие из которых она сочиняла 
  сама: &quot;Черный кот&quot;, &quot;Мадам Лулу&quot;. Изу Кремер считают даже 
  родоначальницей песен шуточного и интимного характера. Это направление поддержала 
  в советское время Клавдия Шульженко, а затем и многие другие исполнители.<br>
  <br>
  Завоевав большую популярность, она отправилась уже в Петроград, в Москву - и 
  всюду успех. Нотные магазины были заполнены нотами песен из ее репертуара и 
  большими фотографиями исполнительницы. Но вот в России произошла революция. 
  До прихода новой власти Иза Яковлевна продолжала активно выступать. В это время 
  в Одессу приехал Александр Вертинский. Известный певец выступал тогда в Москве. 
  В первое время после революции в столице жить было трудно, даже известным людям 
  угрожал голод. Вертинский получил предложение приехать на гастроли в Одессу. 
  Вот что вспоминает знаменитый артист: &quot;Я сильно побаивался за свой успех 
  в этом своеобразном городе. Одесситы - большие патриоты, у них свои особые вкусы, 
  они имеют своих актеров, которых очень любят, и признают &quot;привозных&quot; 
  очень осторожно и неохотно. Тем более что у них была своя собственная &quot;звезда&quot; 
  в песенном жанре - Иза Кремер, довольно талантливая исполнительница французских 
  и немецких песенок, переведенных на русский язык, а также еврейских. Разница 
  между нами была та, что она пела чужие песни, а я - свои собственные, ну и в 
  различии жанров, конечно. Муж ее был главным редактором самой крупной газеты 
  - &quot;Одесские новости&quot;, и я боялся, что эта газета мне &quot;не даст 
  ходу&quot;. Однако этого не случилось. Иза, с которой я был знаком в Москве, 
  пришла на мой концерт, много аплодировала мне, демонстрируя свою лояльность. 
  Она привела с собой даже мужа - всесильного редактора Хейфеца. Публика приняла 
  меня тепло, и отзывы в газетах на другой день были прекрасные. У меня до сих 
  пор сохранилась рецензия Эдуарда Багрицкого, тогда скромного одесского репортера&quot;. 
  <br>
  <br>
  Но время буржуазной Одессы уходило. Когда советская власть установилась в городе, 
  местной интеллигенции пришлось нелегко. Многим интеллектуалам грозило физическое 
  устранение. Иза Яковлевна приложила руку к спасению от неминуемой гибели популярного 
  в Одессе поэта и переводчика Александра Биска. Вот что он рассказывал впоследствии 
  об этой истории: &quot;Я был арестован в качестве заложника. Просидел я, впрочем, 
  недолго, всего 3 дня. В это время печаталась моя книга из Рильке (речь идет 
  о переводе австрийского поэта Райнера-Марии Рильке. - &quot;СП&quot;). По гениальнейшему 
  совету Изы Кремер моя жена забрала из типографии первый экземпляр моей книги 
  и пробралась с ним к самому председателю ЧК Калиненко, чтобы доказать, что я 
  не буржуй. Меня, в конце концов, освободили, но секретарь ЧК товарищ Веньямин, 
  нежный юноша с голубыми глазами (говорили, что он собственноручно расстреливает 
  людей), потребовал взамен освобождения мою книгу с надписью&quot;.<br>
  <br>
  Александр Биск после этого срочно уехал на Запад. Его примеру последовала и 
  Иза Кремер. В 1919 г. вместе со своим мужем, редактором &quot;Одесских новостей&quot; 
  Хейфецом, она эмигрирует сначала в Константинополь, а затем во Францию. В Константинополе 
  она около года работала в артистическом заведении Юрия Морфесси, также известного 
  певца. В его кабаре Иза когда-то пела еще в Одессе. Приехав во Францию Кремер 
  ушла от Хейфеца. Познакомившись с известным американским импресарио, она начинает 
  гастролировать по многим странам мира, приобретая мировую известность. В 20-х 
  годах она побывала с гастролями в Румынии: Бухарест, Кишинев (известна даже 
  точная дата ее приезда - 4 мая 1929 года), Бельцы, Измаил. Выступала она и Берлине, 
  Париже, Милане, Риме, Нью-Йорке. Она достигла пика славы, став популярной и 
  как киноактриса.<br>
  <br>
  О ее известности упоминал, находясь в Нью-Йорке, Сергей Есенин в одном из писем 
  своему другу, поэту и прозаику Анатолию Мариенгофу: &quot;Раньше подогревало 
  то при всех российских лишениях, что вот, мол, &quot;заграница&quot;, а теперь, 
  как увидел, молю бога не умереть душой и любовью к моему искусству. Никому оно 
  не нужно, значение его для всех - как значение Изы Кремер, только с тою разницей, 
  что Иза Кремер жить может на свое пение, а тут хоть помирай с голоду&quot;. 
  Сохранилось воспоминание одной из зрительниц концерта Изы Кремер в Париже: &quot;Я 
  видела и слышала нежную, очень артистичную Изу Кремер. Она выступала в приталенном 
  чёрном платье и пела &quot;Мадам Лулу&quot; и &quot;Чёрного Тома&quot;. Это 
  было в зале Гаво, в Париже, и концерты собирали много народу. Иза была среднего 
  роста, слегка полноватая, носила глубокое декольте&quot;.<br>
  <br>
  Кстати, великий комбинатор Остап Бендер, пребывая в добром расположении духа, 
  любил напевать известную песню, написанной Кремер на мелодию аргентинского танго, 
  - &quot;Под знойным небом Аргентины&quot;. В конце 30-х годов она практически 
  прекращает публичные выступления. Исключение было сделано только в одном единственном 
  случае - перед главами СССР, США и Великобритании, прибывшими на Тегеранскую 
  конференцию 1943 года. У Уинстона Черчилля был тогда день рождения и в качестве 
  подарка он Сталина просил привезти на концерт великого тенора Вадима Козина. 
  А для советского вождя он пригласил Изу Кремер. Рядом с ней также выступали 
  актриса Марлен Дитрих и французский шансонье Морис Шевалье. За кулисами тогда 
  Иза успела шепнуть Вадиму Козину: &quot;Попроси у Черчилля остаться - и весь 
  мир будет у твоих ног&quot;. Козин отказался и вернулся назад в Москву. Через 
  год за отказ петь о Сталине его осудили и выслали в Магадан. <br>
  <br>
  Последние годы ее жизни проходили в Аргентине. Иза, вступив в общество аргентино-советской 
  дружбы, даже готовилась приехать на родину, но за несколько дней до предполагавшегося 
  отъезда ее не стало. Она умерла 7 июля 1956 года в аргентинском городе Кордова. 
  Изу Кремер незаслуженно относят к числу &quot;забытых кумиров&quot;. Но еще 
  в большей мере несправедливо то, что о ней не помнят на ее родине. Сколько еще 
  времени понадобится, чтобы исправить историческую несправедливость? <br>
  <br>
