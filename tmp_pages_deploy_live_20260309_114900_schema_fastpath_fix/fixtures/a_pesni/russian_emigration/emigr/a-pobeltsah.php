	<strong>Елена Замура </strong><br>
  <br>
<span class="header"><strong>ПЕСНЯ О БЕЛЬЦАХ БЫЛА ВПЕРВЫЕ ИСПОЛНЕНА НА БРОДВЕЕ </strong><br></span>
  <br>
  <br>
  У города Бельцы есть свой гимн. В отличие от песни про Кишинев «Мой белый город», 
  официально его никогда не признавали. Зато этот хит поют и бельчане, и эмигранты 
  из Бельц по всему миру. <br>
  <br>
  Как вы уже поняли, эта знаменитейшая песня – не что иное, как «Майн штэйтэлэ 
  Бэлць» («Мой городок Бельцы»). Одно время – правда, давно - она даже входила 
  в репертуар Филиппа Киркорова, чьи предки родом из Бессарабии. Между прочим, 
  похоронены Киркоровы в Кишиневе на армяно-католическом кладбище близ Долины 
  Роз. Несколько лет назад отец Филиппа Бедрос Киркоров даже навещал эти могилы. 
  <br>
  <br>
  Что касается песни о Бельцах - мало кто знает о том, что ее на идише написали 
  специально для знаменитой певицы Изы Крeмер. Слова - поэта Якова Якобса, музыка 
  - композитора Александра Ольшанецкого. Иза впервые и исполнила «Майн штэйтэлэ 
  Бэлць» в мюзикле, поставленном на Бродвее. <br>
  <br>
  <img src="iza1.jpg" alt="Иза Кремер" width="199" height="323"> <img src="iza2.jpg" alt="Иза Кремер" width="246" height="325"><br>
  <br>
  Родилась будущая артистка кино, оперы и оперетты Изабелла Кремер в Бельцах 7 
  июля 1889 года. Родители с ранних лет поняли, что, вложив в образование Изабеллы 
  скудные семейные средства, не прогадают. Это было мудрое решение. В 1912 году 
  девушка отправилась в Милан, где училась пению у известного педагога Луиджи 
  Ронци и даже выступала в небольших театрах. <br>
  <br>
  Через два года Иза вернулась в Бельцы. Её приглашают в знаменитый одесский оперный 
  театр. Молодая певица выступает и в оперетте. Она имеет огромный успех, гастролируя 
  по крупнейшим городам России. Выходит замуж за редактора «Одесских новостей» 
  Хейфеца...<br>
  <br>
  Естественно, все испортила революция. Большевикам была не нужна Иза Кремер. 
  Изе Кремер были не нужны большевики. В 1919 году певица эмигрирует сначала в 
  турецкий Константинополь, потом во Францию. Разводится с несчастным одесситом. 
  Много гастролирует по Европе, снимается в кино, завоевывает мировую известность. 
  <br>
  <br>
  В 1943 году уже немолодая, но прославленная Иза Кремер приглашена Черчиллем 
  специально для того, что петь для Сталина. Главы трех великих держав – СССР, 
  США и Великобритании - встречаются на Тегеранской конференции, чтобы обсудить 
  ход войны и судьбы мира. Для Сталина, Черчилля и Рузвельта поют четверо легендарных 
  исполнителей 30-40-х: немка Марлен Дитрих, француз Морис Шевалье и русский Вадим 
  Козин. Кстати, для Козина это закончилось пожизненной ссылкой в Сибирь… <br>
  <br>
  До сих пор выбор Черчилля поражает. Изу Кремер, о которой мы сегодня практически 
  ничего не знаем, он поставил в один ряд с исполнителями, у которых были миллионы 
  поклонников! <br>
  <br>
  Но Сталин эмигрантов не одобрял и Изу не простил. В советские времена ее имя 
  окружало гробовое молчание. Правда, на волне увлечения Аллой Баяновой вышел 
  диск с хитами Изы и Аллы. До сих пор о жизни певицы-бельчанки известно очень 
  мало. Быть может, когда-нибудь поклонники восполнят этот пробел. <br>
  <br>
  <img src="iza.jpg" alt="Иза Кремер" width="200" height="150"> <img src="marlen.jpg" alt="Марлен Дитрих" width="200" height="151"> 
  <img src="moris.jpg" alt="Морис Шевалье" width="200" height="151"> <img src="kozin.jpg" alt="Вадим Козин" width="200" height="151"><br>
  <br>
  После войны Иза Кремер жила в Аргентине – стране, в которую в 20-40-х годах 
  прошлого века эмигрировало пол-Европы. К этому времени певица уже перестала 
  выступать. Она обосновалась в городе Кордова. У Кремер было все – деньги, слава, 
  почет. Но не было, увы, душевного покоя. <br>
  <br>
  Когда есть все, хочется родины. Иза, которая жила в самых роскошных городах 
  Европы и Америки, хотела в Бельцы!<br>
  <br>
  Алла Баянова, по ее словам, тоже мечтала о возвращении в СССР – но очень хорошо 
  понимала, что торопиться некуда. Вернулась она только в 80-х. Это было мудро. 
  Наивная Кремер заторопилась в Союз, когда умер Сталин. Она думала, что конец 
  диктатора означает конец диктатуры. Ангел-хранитель певицы, очевидно, знал о 
  советских порядках (например, о борьбе с евреями-«космополитами») больше, чем 
  Иза, и не одобрил ее решения. Знаменитая артистка скончалась 7 июля 1956 года 
  в возрасте 67 лет. Она уже собрала чемоданы и взяла билет. До отъезда в СССР 
  оставалось несколько дней. <br>
  <br>
  Иза не увидела перед смертью свой любимый городок Бельцы. И слава Богу. Она 
  была бы разочарована. Война и две волны депортации изменили облик «Северной 
  столицы». В ней осталось не так уж много еврейского населения, придававшего 
  до шарм этому городку. Впереди была массовая эмиграция 70-90-х. Уехали почти 
  все евреи и многие неевреи, обосновавшись в Израиле и США. Среди них очень мало 
  людей, которые оставались бы равнодушными при звуках знаменитой мелодии песни 
  «Майн штэйтэлэ Бэлць». <br>
