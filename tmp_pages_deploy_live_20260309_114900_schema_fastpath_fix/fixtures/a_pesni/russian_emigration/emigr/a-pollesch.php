  а имя Шленгеля перекочевало из польской пластинки. Так или иначе, Пётр Лещенко 
  включил эту прекрасную удалую песню в свой репертуар и напел её на пластинку 
  в рижской фирме &quot;Беллаккорд Электро&quot; в 1938 году, это исполнение принесло 
  песне мировую известность.<br>
