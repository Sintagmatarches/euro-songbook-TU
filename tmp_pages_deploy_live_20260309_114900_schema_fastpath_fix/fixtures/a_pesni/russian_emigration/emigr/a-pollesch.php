  Паулина была пианисткой, отец Якуб - певцом, брат Иосиф - скрипачем, другой 
  брат Станислав и сестра - пианистами, двоюродные братья Артур и Генрих Гольды 
  были музыкантами и композиторами. Альберт Плакс пишет:&quot; Ежи родился ... 
  под фамилией Мелодиста. Как и почему он стал Петерсбурским, как это связано 
