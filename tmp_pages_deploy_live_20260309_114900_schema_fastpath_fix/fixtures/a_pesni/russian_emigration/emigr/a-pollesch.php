  <strong>&quot;ВСЕ ЗА ЛЮБОВЬ Я ПРОЩАЮ&quot;</strong><br>
  <br>
  Песни, они - как птицы и облака, не признают государственных границ, пересекают 
  их без виз и паспортов. B 1933 году Петр Лещенко включил в свой репертуар ещё 
  одну польскую песню, вальс-бостон &quot;Любовь тебе всё простит&quot; в русском 
  переводе под названием &quot;Всё за любовь я прощаю&quot; (или &quot;Милость&quot;). 
  Pаспостранилась она по всему миру на пластинках фирм &quot;Columbiа&quot; и 
  &quot;Bellaccord Electro&quot;, причём на пластинках венского филиала &quot;Коламбии&quot; 
  песня называлась &quot;Милость&quot; (по первому слову польского названия песни: 
  &quot;Милосьць ци вшыстко выбачы&quot;), а на рижских пластинках - &quot;Всё 
  за любовь я прощаю&quot;. Сочинили эту песню специально для польского фильма 
  &quot;Шпион в маске&quot; композитор Генрих Варс (собственно Варшавский) и (под 
  псевдонимом &quot;Ольден&quot;) выдающийся польский поэт Юлиан Тувим. В фильме 
  песню исполняла звезда польской эстрады Ханка Ордонувна - певица, которую поляки 
  ласково называли &quot;наша Ордонка&quot;. Фильм c этой песней вышел на экраны 
  в 1933 году.<br>
  <br>
  Выдающийся композитор Генрих Варс (1902-1977) сочинял, в основном, музыку к 
  фильмам. Им были созданы многие песни, ставшие шлягерами. В 1939 году он, офицер 
  запаса, воевал, бежал из немецкого плена в оккупированный Красной Армией Львов, 
  где создал знаменитый &quot;Львовский Теа-джаз Генриха Варса&quot; Коллектив 
  Варса гастролировал по республикам СССР в 1940-41 годах (Одесса, Семфирополь, 
  Киев, Харьков, Москва, Ленинград, Ташкент) уже с русским репертуаром. Некоторые 
  из польских песен Варса были переведены на русский язык (&quot;Прощальная песенка 
  Львовского джаза&quot;, &quot;Ничего не знаю&quot;, &quot;Первый знак&quot;, 
  &quot;Я условился с ней точно в девять&quot;). Его &quot;жестокое&quot; русское 
  танго &quot;Забвение&quot; исполняли Альберт Гарис, Eвгений Бодо, а много лет 
  позже - Иосиф Кобзон. Все эти песни сохранились на пластинках &quot;Апрелевского 
  завода&quot;. О некоторых песнях из репертуара &quot;Теа-джаза&quot; сейчас 
  мы можем узнать только по сохранившимся афишам того периода. После начала Отечественной 
  войны Варс и его музыкальный коллектив были включены в состав Польской Армии 
  генерала Андерса, a после войны композитор эмигрировал в США.<br>
  <br>
  Юлиан Тувим (1894-1953) - один из самых выдающихся польских поэтов первой половины 
  минувшего столетия. Сочинение эстрадных песенок он не считал для себя серьёзным 
  занятием, написал их несколько десятков, но чаще всего под разными псевдонимами. 
  Тексты песен всегда создавал для конкретных исполнителей. Для Ханки Ордонувны 
  сочинил не только вальс &quot;Всё за любовь я прощаю&quot;, но и ряд других 
  песен, ставших шлягерами, например, &quot;На первый знак&quot;, &quot;Песня 
  любви&quot;, &quot;Момент перед сумерками&quot;, &quot;Мимоза&quot;, &quot;Полюби 
  меня страстно&quot;, &quot;Старая песенка&quot; и др. <br>
  <br>
  С особенной экспрессией Ордонувна исполняла &quot;русские&quot; песни Тувима 
  - &quot;Княгинюшкy&quot; и &quot;Шарлатанкy&quot; (перепев известной песни &quot;Ах, 
  шарабан мой...&quot;). Песни Тувима &quot;Что нам осталось с тех лет&quot; и 
  &quot;Марыся&quot; хор Дана исполнял по-польски и по русски. <br>
  <br>
