  <br>

	<span class="header"><strong>ПОЛЬСКИЕ ПЕСНИ В РЕПЕРТУАРЕ ПЕТРА ЛЕЩЕНКО </strong><br></span>
  <br>
  <em>3 июля 1898 года родился Петр Константинович Лещенко. Легенде русского шансона 
  посвящается.</em><br>
  <br>
   <em>Первая публикация - <a href="http://www.shanson.org/forum/showthread.php?t=1644" target="_blank">Шансон 
  - Портал</a>, 30 июня - 1 июля 2009 года.</em><br>
 <br>
  <br>
  <strong>ВВЕДЕНИЕ</strong><br>
  <br>
  Путь Петра Лещенко из Бухареста в Ригу проходил всегда через польскую столицу. 
  В Варшаве певец часто останавливался на короткий срок, чтобы познакомиться с 
  новыми шлягерами, появляющимися на польской эстраде. Лучшие из них брал в свой 
  репертуар, чаще всего с собственным переводом польского текста на русский язык. 
  Тема польской эстрады в творчестве Петра Лещенко ещё до конца не исследована. 
  Возможно, неизвестные сведения о посещениях певцом Польши кроются где-то в подшивках 
  старых довоенных газет. О гастролях Петра Лещенко в Польше упоминает кишинёвский 
  историк Сергей Суляк, но подтверждений этому не приводит, и в других источниках 
  я не встретил этих фактов. По постоянному интересу, который проявлял Пётр Константинович 
  к творчеству польских композиторов Ежи Петербурского, Шимона Каташка, братьев 
  Артура и Гeнриxа Гольдов, Зигмунта Карасинского, Гeнриxа Варса, можно предположить, 
  что он был лично знаком с ними.<br>
  <br>
  Мировым центром российской эмиграции в первой половине прошлого века был Берлин, 
  в нём проживало полмиллиона выходцев из России. Не уступали Берлину по количеству 
  русских эмигрантов Париж, Лондон, Прага. По сравнению с ними Варшава выглядела 
  глухой провинцией. В Польше проживало всего около 140 тысяч русских эмигрантов. 
  В основном была это первая волна эмиграции, попавшей в Польшу после октябрьского 
  переворота 1917 г., когда власть в России захватили большевики. Бежали в Польшу 
  офицеры и солдаты Белой Армии после безуспешной попытки спасти родину в боях 
  гражданской войны, от ужасов красного террора бежали и искали пристанища у чужого 
  костра дворяне, интеллигенция и другие потенциальные жертвы большевизма. В стране, 
  большая часть которой в течение 100 лет была западной окраиной Российской империи, 
  русский язык знали почти все, поляки были знакомы с русской культурой и высоко 
  её ценили. Можно утверждать, что в Польше любовь к русской культуре, особенно 
  к русским песням, была повсеместна и мирно сочеталась с искренней неприязнью 
  к российской государственной машине, то ли царской, то ли большевистской. Символами 
  старой России в глазах поляков оставались беспредельность русских степей, тройка, 
  цыганский хор у &quot;Яра&quot;, балалайка и самовар.<br>
  <br>
  Эта символика находила отражение в русских песнях и романсах, звучавших с грампластинок. 
  Залетела в Варшаву из Берлина &quot;Синяя птица&quot;, эмигрантский театр миниатюр 
  Виктора Хенкина. А без &quot;Синей птицы&quot; не было бы в Польше обрусевшего 
  венгра Фредерика Яроши, который задержался в Варшаве на долгие годы и стал душой 
  кабаре &quot;Qui Pro Quo&quot;, знаменитой кузницы артистических талантов. Это 
  он, Яроши, магическим взглядом чёрных очей мгновенно покорил сердце Ольги Чеховой 
  и увёз её собой из охваченной послереволюционным хаосом Москвы. Много лет проживал 
  в Польше Юрий Морфесси, в фирме &quot;Syrena Electro&quot; записал он на пластинки 
  несколько десятков песен, аккомпанировал ему князь Сергей Голицын. Выступала 
  в Варшаве с концертами Иза Кремер и задержалась здесь надолго. О связях с Польшей 
  Александра Вертинского, полюбившего эту страну, много написано. Жил он в Варшаве 
  несколько лет, оставил яркий след на польской эстраде. Но особенно способствовали 
  популяризации русской эстрады, особенно песен из репертуара Петра Лещенко, выступления 
  знаменитого хора Григория Семенова. На пластинках фирмы &quot;Syrena Electro&quot; 
  сохранилось около 70-ти записей этого хора русских эмигрантов.<br>
  <br>
  Имя Петра Лещенко было известно во всей стране, благодаря песням на пластинках 
  фирмы &quot;Columbia&quot;, выпускал их также варшавский филиал этой английской 
  фирмы. Выпускала пластинки с песнями Петра Лещенко популярная в Польше фирма 
  &quot;Odeon&quot;, фирма &quot;Cristal Electrо&quot; в 1934 г. выпустила пластинку 
  с его песнями &quot;Трошка&quot; и &quot;Не плачь, мой беби&quot;. В январе 
  1939 г. польская фирма &quot;Syrena Electro&quot; выпустила 7 пластинок с песнями 
  популярного певца: &quot; Тринцы-бринцы&quot;, &quot;Тпру ты, дутый&quot;, &quot;Моя 
  Марусичка&quot;, &quot;Метелица&quot;, Капризная&quot;, &quot;Туманно на душе&quot;, 
  &quot;Увяли грёзы&quot;, &quot;Эй, друг-гитара&quot;, &quot;Трошка&quot;, &quot;Песнь 
  гитары&quot;, &quot;Возле леса&quot;, &quot;Марфуша&quot;, &quot;Рюмка водки&quot; 
  и &quot;Не плачь, мой беби&quot;. Были это перепечатки с матриц рижской фабрики 
  &quot;Bellaccord Еlectro&quot;. Довоенное поколение поляков было хорошо знакомо 
  с творчеством русского артиста, имеющего мировую славу. Многие польские исполнители 
  включили в свой репертуар его лучшие песни с польскими текстами, исполняли их 
  и на русском языке.<br>
  <br>
  Популярность русских песен и романсов в Польше была столь велика, что многие 
  польские композиторы сочиняли &quot;русские танго&quot;, &quot;русские фокстроты&quot;, 
  &quot;русские вальсы&quot;, используя русскую экзотику. Так обычно значилось 
  на этикетках пластинок с этими песнями. И в наши дни поляки с удовольствием 
  слушают старые записи песен &quot;Андрюша&quot;, &quot;Ах, те цыганки&quot;, 
  &quot;Батюшка&quot;, &quot;Ванька&quot;, &quot;Гармошка&quot;, &quot;Катюша&quot;, 
  &quot;Княгинюшка&quot;, &quot;Когда течёт Волга&quot;, &quot;Мы казаки&quot;, 
  Наташа&quot;, &quot;Наташа танцует&quot;, &quot;Ностальгия&quot;, &quot; О любви 
  не говори мне&quot;, &quot;Пей, Гришка&quot;, &quot;Семечки&quot;, &quot;Соня&quot;, 
  &quot;Тамара&quot;, &quot;Танцуй, Машка&quot;, &quot;Товарищ Соня&quot;, &quot;У 
  самоварa&quot;, &quot;Шарлатанка&quot;, &quot;Хандра&quot;, &quot;Я пью чай&quot; 
  и многие другие песни. Лучшие образцы этих польских &quot;песен с русской душой&quot; 
  Пётр Лещенко включил в свой репертуар, заменяя польские тексты русскими.<br>
  <br>
  В послевоенные годы партийная цензура в Польше наложила запрет на песни Петра 
  Лещенко. Имя его не упоминалось в печати. Но я был удивлён, впервые попав в 
  Польшу, когда по радио слышал знакомые с детства лещенковские мелодии. Звучали 
  старые пластинки довоенных певцов с песнями из репертуара Петра Лещенко, на 
  них запрет цензуры не распространялся. Но даже в период перестройки, когда я 
  был гостем на польском радио у музыкального тандема Дануты Желеховской и Яна 
  Загозды, ведущих цикличных передач &quot;Проходят года - остаются песни&quot; 
  и &quot;Сладкое радио-ретро&quot;, я убедился, что в фондах польского радио 
  пластинок Петра Лещенко нет. Изредка звучали они в передачах, когда Ян Загозда 
  приносил пластинки из своей домашней коллекции. Благодаря этим передачам, когда 
  тиски партийной цензуры начались постепенно разжиматься, с творчеством Петра 
  Лещенко познакомилось послевоенное поколение поляков.<br>
  <br>
  На польском &quot;Радио - Бис&quot; несколько лет велась замечательная передача 
  &quot;Атлас Русской Песни&quot;, вели её Илья Змеев, радиожурналист из Перми 
  и полька Малгожата Здун. Песен Петра Лещенко в фонотеке передачи долго не было, 
  несколько раз с песнями из его репертуара выступал в передаче русский певец, 
  поэт и композитор Игорь Славич, живущий и концертирующий в Варшаве. Лишь в 96-м 
  году, когда одна из слушательниц принесла в редакцию две первые изданные в СССР 
  долгоиграющие пластинки фирмы &quot;Мелодия&quot;, эти песни прозвучали в эфире, 
  а корреспондент радио &quot;Маяк&quot; на польском радио Людмила Львова рассказала 
  слушателям о трагической судьбе Петра Лещенко.<br>
  <br>
  К столетнему юбилею Петра Лещенко &quot;Сладкое Радио-ретро&quot; посвятило 
  песенному наследию певца трёхчасовую передачу, в которой наряду с русским вариантом 
  песен в исполнении Петра Лещенко, звучали также их польские варианты.<br>
  <br>
  <br>
  <strong>ГИМН МАТЕРИНСКОЙ ЛЮБВИ. НЕУВЯДАЕМОЕ ТАНГО<br>
  &quot;СЕРДЦЕ МАМЫ&quot;</strong><br>
  <br>
  Мне песня &quot;Сердце мамы&quot; дорога тем, что она была первой из песен Петра 
  Лещенко, которые мне посчастливилось услышать в начале 50-х годов минувшего 
  века. Когда сейчас я слышу мелодию этого чудесного танго, в моей памяти возрождаются 
  образы из дней далёкой юности. Я вспоминаю маленький шахтёрский посёлок, затерянный 
  где-то в донецких степях. В нашей школе во время большого перерыва девчонки 
  из старших классов танцевали под музыку, воспроизводимую с граммофонных пластинок 
  школьного радиоузла. Кто-то из учеников принёс в радиоузел чёрный кружок из 
  рентгеновской плёнки с кустарной записью, из висевшего на стене динамика лилась 
  прекрасная мелодия танго и звучал проникающий в душу мягкий баритон неизвестного 
  певца. Знатоки шептали: &quot;Это Петр Лещенко! Запрещённый!&quot; Педагоги 
  не проявили должной бдительности и несколько дней подряд во время большой перемены 
  голос крамольного певца звучал в советской школе, текст его песни все уже знали 
  наизусть:<br>
  <br>
  <em>Кошмарной темной ночью<br>
  Забилось сердце дрожью.<br>
  Я потеряла любовь мою,<br>
  И ты оставил меня одну.<br>
  <br>
  Мой славный, мой сыночек,<br>
  Забыл ты маму свою.<br>
  Тебя молю я вернуть покой мне,<br>
  И слёзно я молю к тебе...<br>
  <br>
  Бедное сердце мамы<br>
  Еле стучит в груди.<br>
  Бедное сердце мамы<br>
  Ищет покой в тиши.<br>
  <br>
  Доктора не зовите,<br>
  Сына мне возвратите.<br>
  С ним хоть один часок пpобыть...<br>
  Как тяжело мне жить!</em><br>
  <br>
  Я не умел танцевать, стоял у подоконника и слушал. И так мне жалко было бедную, 
  покинутую сыном маму.<br>
  <br>
  Прошло почти полвека, канули в Лету те, обязанностью которых было &quot;тащить 
  и не пущать&quot;, постепенно разжимались челюсти партийной цензуры, и песни 
  опального певца изредка стали появляться на антенне &quot;Радио России&quot;. 
  По просьбам слушателей песни Петра Лещенко стал включать в свою цикличную передачу 
  &quot;Встреча с песней&quot; Виктор Витальевич Татарский. Когда к автору передачи 
  обратилась слушательница из Рыбинска Елена Григорьевна, внучка князя Феликса 
  Юсупова с просьбой передать &quot;одну из песен Петра Лещенко, но самую хорошую&quot;, 
  Татарский выбрал для неё танго &quot;Сердце мамы&quot;. Влюблённая с детства 
  в песни Петра Лещенко, Елена Григорьевна так писала в своём письме (&quot;Встреча 
  с песней&quot; Nr 745, 30.10.1997 г.): &quot;Мне хотелось бы узнать, почему 
  он так рано умер? Есть у меня мечта, но она несбыточна: поехать в Румынию, купить 
  на все деньги красивых роз и положить на его могилу...&quot; Я подумал, удивилась 
  бы Елена Григорьевна, если бы узнала, что её любимому певцу когда-то на концерте 
  в Лондоне аккомпанировал её дедушка, князь Юсупов.<br>
  <br>
  Может быть не всем известно, что танго &quot;Сердце мамы&quot; родилось на польской 
  земле. Его авторами были композиторы Зигмунт Карасинский и Шимон Каташек, создатели 
  и руководители одного из самых популярных в Варшаве танцевальных оркестров в 
  начале 30-х годов прошлого века. Были они друзьями с юности, вместе начинали 
  музыкальную карьеру, вместе создали множество замечательных песен.<br>
  <br>
  Зигмунт Карасинский (1898-1973), композитор, скрипач-виртуоз, он объездил с 
  гастролями полмира, осенью 1939 г. в советском Белостоке создал Первый Белорусский 
  Джаз. Но советская оккупационная зона ему не понравилась, он, как и многие другие 
  польские артисты, перебрался из СССР в немецкую зону. Ему, по проcxождению еврею, 
  чудом удалось пережить войну, укрываясь в селе на юге Польши. В 60-х годах Карасинский 
  эмигрировал на Запад, умер в нищете в приюте для бездомных в Копенгагене.<br>
  <br>
  Шимон Каташек (1898-1943) композитор, выдающийся пианист, почти все свои музыкальные 
  произведения создавал в соавторстве с Карасинским, вместе с ним организовал 
  свой первый оркестр &quot;Золотой джаз&quot;. Каташек во время немецкой оккупации 
  руководил оркестром в Варшавском Гетто, был расстрелян фашистами при ликвидации 
  узников Павяка.<br>
  <br>
  Петр Лещенко назвал своё танго &quot;Сердце мамы&quot;, a польское танго называлось 
  &quot;Serce matki&quot; (&quot;Сердце матери&quot;), польский текст сочинил 
  в начале 30-х годов поэт-песенник Леон Шмарагд, автор многих шлягеров, пользовавшихся 
  большой популярностью на польской эстраде. Стихи Петра Лещенко самостоятельны, 
  они не являлись переводом польского варианта танго. Шмарагд ведёт рассказ от 
  имени своего лирического героя (сына или дочери матери в зависимости от исполнителей 
  песни):<br>
  <br>
  <strong><em>Serce matki</em></strong><em><br>
  <br>
  Żyjemy wśród zamętu i braku sentymentu...<br>
  Tu sztuczny śmiech, tam znów sztuczne łzy,<br>
  obłuda, fałsz - to są życia gry.<br>
  Miłości szczerej nie ma, epoka kłamstw, krzywd i mąk.<br>
  Nikt nie jest sobą, czas rządzi tobą,<br>
  okrutna obojętność wkrąg...<br>
  <br>
  (Перевод:<br>
  Живя в суматохе, мы растеряли наши чувства. Здесь - пустой смех, там притворные 
  слёзы. Среди всеобщего равнодушия фальшь и лицемерие стали основным принципом 
  нашей жизни, нет искренней любви в эпоху лжи, обид и мучений.)</em><br>
  <br>
  Но в этом жестоком мире есть тихая пристань, где можно найти убежище от бессердечия 
  окружающего мира. Этa пристань - сердце матери:<br>
  <br>
  <em>Jedynie serce matki uczuciem zawsze tchnie,<br>
  Jedynie serce Matki o wszystkim dobrze wie.<br>
  Dać trochę ciepła umie i każdy ból rozumie.<br>
  A gdy przestaje dla nas bić,<br>
  Tak ciężko, ciężko żyć...<br>
  <br>
  (Перевод: <br>
  Лишь только сердце мамы всегда любви полно,<br>
  Лишь только сердце мамы знает о всём само.<br>
  Своим теплом согреет, и нашу боль развеет,<br>
  Когда не сможет с нами быть,<br>
  Как тяжело нам жить...)<br>
  </em><br>
  Первыми в 1932 году записали на пластинку &quot;Сердце матери&quot; хор &quot;Эриана&quot; 
  и хор &quot;Дана&quot;. Напели песню на пластинки самые популярные польские 
  певцы: Мечислав Фогг, Адам Астон, Вера Гран и др. И сейчас каждый год 26 мая 
  в &quot;День Матери&quot; эта песня передаётся в эфир многими польскими радиостанциями. 
  <br>
  <br>
  Но ни одному из польских исполнителей не удалось достигнуть уровня исполнительского 
  мастерства Петра Лещенко. Всегда, когда танго &quot;Сердце мамы&quot; звучит 
  в исполнении легендарного певца, на душе становится светлей.<br>
  <br>
  <br>
  <strong>УДИВИТЕЛЬНАЯ ИСТОРИЯ ПЕСНИ<br>
  &quot;У САМОВАРА&quot;</strong><br>
  <br>
  Любители старых песен помнят эту бесхитростную песенку. В её задорной мелодии 
  слышны ритмы одесских улиц. Многие в России не знают, что родилась эта песня 
  на польской земле. Была она популярна во всех странах мира, куда попадали пластинки 
  Петра Лещенко. Записал он эту песню на пластинки в студии венского филиала фирмы 
  &quot;Columbia&quot; в 1933 году. В репертуаре Петра Лещенко имеется несколько 
  польских песен, но &quot;У самовара&quot; - песня особенная, песня с удивительной 
  судьбой.<br>
  <br>
  Написана она была на мелодию, которую сочинила в конце двадцатых годов одарённая, 
  но не получившая музыкального образования и не знающая даже нотной записи девушка, 
  варшавянка Фаина Квятковская (1914-1991) (её настоящее имя - Фeйга Йофе). Родилась 
  она 23 декабря 1914 годa в Ялте в еврейской семье. (В некоторых статьях или 
  биографических словарях год рождения Квятковской подаётся по разному: 1904, 
  1910, 1913 или 1914.) Семья её перебралась в Польшу во время гражданской войны, 
  спасаясь от террора большевиков.<br>
  <br>
  Песня &quot;У самовара&quot; была композиторским дебютом девушки. В 1929 г. 
  случайно услышал эту мелодию и написал к ней текст польский поэт-песенник, директор 
  варшавского театра-ревю &quot;Морское око&quot; Анджей Власт и сразу же включил 
  её в программу своего театра. Вот как передана история рождения песни &quot;У 
  самовара&quot;, записанная Василием Выхристенко со слов её автора, жительницы 
  Ленинграда Фаины Марковны Квятковской:<br>
  <br>
  &quot;Тридцатые годы, тихий вечер в Варшаве, в доме собрались гости и 16-летняя 
  дочь хозяина наигрывает шутливую песенку. К концу вечера к великому удовольствию 
  родителей все гости уже распевали на польский манер:<br>
  <br>
  &quot;У самовара я и моя Маша, а на дворе совсем уже темно...&quot; В доме Квятковских 
  в этот вечер был гостем директор одного из варшавских театров и песню 16-летней 
  Фаины он взял для спектакля. Популярность песни росла....&quot; См. статью Василия 
  Выхристенко <a href="http://www.odessitclub.org/publications/won/won_64/won_64_16.pdf" target="_blank">&quot;У 
  самовара на Невском проспекте&quot;</a>, газета &quot;Всемирные одесские новости&quot; 
  Nr 5/2006.<br>
  <br>
  Вот так рождаются легенды.<br>
  <br>
  На самом деле, всё было более обыденно, песня с приведенным русским текстом 
  появилась гораздо позже. Действительно, Анджей Власт зашёл в дом Квятковских 
  на чашку чая, но не было там &quot;распевающих гостей.&quot; Фаина на своём 
  детском пианино исполнила для гостя несколько собственных произведений. Мелодия 
  одного фокстрота, который Фаина назвала &quot;Море&quot;, Власту очень понравилась, 
  вскоре он написал к ней польский текст и включил новую песню, назвав её &quot;У 
  самовара&quot;, в программу своего театра.<br>
  <br>
  Декорацией для этой песни служил установленный на сцене огромный макет самовара, 
  исполняли песню солисты театра &quot;Морское око&quot; Зуля Погожельская и Тадеуш 
  Ольша, звучала она в обозрениях &quot;То, что любит Варшава&quot; и &quot;Путешествие 
  на луну&quot;. В многих источниках встречается информация о том, что песня &quot;У 
  самовара&quot; была создана в 1931 году. Но это не так, по свидетельству польского 
  филофониста Томаша Лерского фирма &quot;Parlophon&quot; в Варшаве записала эту 
  песню на пластинку в 1929 году, а польская фирма &quot;Syrena Electro&quot; 
  - в 1931 году в исполнениях солиста &quot;Морского Ока&quot; Тадеуша Фалишевского 
  и певца Ежи Велина. На этикетках пластинок песня &quot;Pod samovarem&quot; была 
  названа &quot;русским фокстротом&quot; и получила дополнительное, указанное 
  в скобках название - &quot;Новые Бублички&quot;. Исполняли и записали песню 
  на пластинки также певцы Мечислав Фогг, Тадеуш Богданович, Нина Грудзинская, 
  Витольд Рихтер и др. Пластинки с новой песней быстро разошлись по всей стране. 
  Слабый текст Анджея Власта не помешал песне приобрести огромную популярность.<br>
  <br>
  Это заслуга замечательной музыки Фанны Гордон. Такой псевдоним приняла Фаина 
  Квятковская.<br>
  <br>
  Анджей Власт (1895-1943) (собственно, Густав Баумриттер) в молодости был способным 
  журналистом, позже начал сочинять тексты песен по заказам польских композиторов, 
  переводил тексты зарубежных шлягеров и за короткое время сочинил более 2000 
  песен. Создавал он тексты очень быстро, не заботясь о форме и содержании, утверждал, 
  что в каждой песне важна мелодия и образ, песней рождённый, а поэтические тонкости 
  не играют особой роли. Хрустальную вазу мелодии можно заполнить любым содержанием, 
  достаточно найти несколько ударных слов для припева, которые дадут песне силу 
  и привлекательность. Не удивительно, что творчество Власта стало символом поэтической 
  халтуры. В польском литературоведении даже появился термин с уничижительным 
  оттенком: &quot;властовские рифмы&quot;. Жизнь поэта оборвалась трагически, 
  в 1943 году застрелил его немец на улице варшавского гетто. Как это ни странно, 
  сотни песен Власта с текстами невысокого полёта и сейчас всё ещё пользуются 
  любовью у слушателей. Так случилось и с песней &quot;У самовара&quot;. Её текст 
  явно не соответствовал живой, западающей в сердце мелодии. Судите сами, вот 
  польский вариант и мой, почти дословный перевод этой песни:<br>
  <br>
  <em>Znów jest maj, ten sam,<br>
  Kasztany kwitną znów ogromnie.<br>
  Naplewat' mnie tam!<br>
  Ja jedno wiem i jedno pomnę:<br>
  <br>
  Pod samowarem siedzi moja Masza,<br>
  Ja mówię &quot;tak&quot;, a ona mówi &quot;nie&quot;.<br>
  Jak w samowarze kipi milość nasza,<br>
  Ja gryzę pestki, ona na mnie klnie.<br>
  <br>
  Potem nagle po całusku<br>
  Wydziela do herbaty na prikusku.<br>
  Pod samowarem siedzi moja Masza<br>
  I jak herbata tak naciąga mnie!<br>
  <br>
  Przyszli, wzięli nam<br>
  Firanki, szubę, stół i łóżko.<br>
  Naplewat' mnie tam,<br>
  Bo ja o jednym myślę duszko.<br>
  <br>
  Pod samowarem siedzi moja Masza....<br>
  <br>
  </em>Перевод:<em><br>
  <br>
  Снова май тот сам,<br>
  Каштаны буйно расцветают.<br>
  Наплевать мне там!<br>
  Одно лишь знаю, об одном мечтаю.<br>
  <br>
  У самовара сидит моя Маша,<br>
  Скажу ей &quot;да&quot;, она мне &quot;нет&quot; твердит.<br>
  Как в самоваре, так кипит страсть наша,<br>
  Она ругается на чём свет стоит.<br>
  <br>
  После поцелуй горячий<br>
  Она подарит мне вприкуску к чаю.<br>
  У самовара сидит моя Маша,<br>
  Как сладкий чай, она влечёт меня.<br>
  <br>
  Пришли, забрали нам<br>
  Гардины, шубу, стол и раскладушку.<br>
  Наплевать мне там,<br>
  Я об одном лишь помню, моя душка.<br>
  <br>
  У самовара сидит моя Маша...</em><br>
  <br>
  Даже с таким слабым текстом песня сразу же стала шлягером, звучала в каждой 
  семье, у которой был патефон, звучала на эстрадах по всей стране. Когда представители 
  фирмы &quot;Polydor Records&quot; обратились к Фаине Квятковской с предложением 
  записать песню для российской эмиграции в Прибалтике, Фаина согласилась и сама 
  написала русский текст песни, имеющий мало общего с текстом Власта. В песне 
  осталось всего две строфы:<br>
  <br>
  <em>У самовара я и моя Маша,<br>
  А на дворе совсем уже темно.<br>
  Как в самоваре, так кипит страсть наша,<br>
  Смеется хитро месяц нам в окно.<br>
  <br>
  Маша чай мне наливает,<br>
  А взор её так много обещает.<br>
  У самовара я и моя Маша,<br>
  Вприкуску чай пить будем до утра.</em><br>
  <br>
  Первым исполнителем, записавшим на пластинки русский вариант песни, был польский 
  певец из Риги Арпалин Нумма. Запись песни &quot;У самовара&quot; в сопровождении 
  оркестра Пауля Годвина была произведена фирмой &quot;Polydor&quot; в 1932-м 
  или 1933-м году.<br>
  <br>
  Пётр Лещенко, записывая эту песню на пластинки, добавил к песне две строфы:<br>
  <br>
  <em>Ночка снежная,<br>
  А у меня на сердце лето.<br>
  Жёнка нежная,<br>
  Пускай завидуют мне это!<br>
  <br>
  Думы мои одне:<br>
  Побыть скорее с ней наедине.<br>
  Тёмна ночка покроет всё,<br>
  Не выдаст нас, и будет точка.</em><br>
  <br>
  Изменилась ещё одна строка, вместо <em>&quot;Смеётся хитро месяц...&quot;</em> 
  появилась строка <em>&quot;И месяц смотрит ласково в окно&quot;</em>.<br>
  <br>
  Только благодаря записи на пластинке Петра Лещенко песня в скором времени стала 
  известна во всём мире. Включил эту песню в свой репертуар Леонид Утёсов, когда 
  пластинка из Риги попала ему в руки. Песню &quot;У самовара&quot; он записал 
  на московской фабрике грампластинок в феврале 1934 г., в СССР она приобрела 
  огромную популярность и быстро, как говорят, &quot;пошла в народ&quot;, появлялись 
  её новые варианты.<br>
  <br>
  Вот, например, один из них:<br>
  <br>
  <em>У самовара я и моя Маша.<br>
  Как хорошо нам быть с тобой вдвоем!<br>
  И пироги, и гречневая каша!<br>
  Сегодня вдосталь мы чайку попьем.</em><br>
  <br>
  Евреи, придавая песне свой национальный колорит, пели: <em>&quot;У самовара 
  я и мой Абраша...&quot;</em> или <em>&quot;У самовара кантор, я и Сара&quot;</em>. 
  Прекрасная мелодия Фанны Гордон прижилась в разных странах. До войны исполнял 
  песню на литовском языке и записал на пластинку известный певец русской эмиграции 
  Даниил Дольский. Песня называлась <em>&quot;Palangos juroj...&quot;</em> и по 
  содержанию ничего общего с нашей Машей не имела:<br>
  <br>
  <em>Palangos juroj nuskendo mano meile<br>
  Ir saltos bangos jos neatiduos... <br>
  <br>
  (В Паланге в море утонула любовь моя<br>
  и холодные волны не вернут её...)</em><br>
  <br>
  Есть и другое литовское исполнение этой песни - певца Андрюса Мамонтоваса (или 
  по-русски - Андрея Мамонтова).<br>
  <br>
  Но судьба этой песни в СССР - тема слишком обширная. Она подробно освещена в 
  многочисленных статьях, которые без труда можно найти в Интернете. Отмечу лишь, 
  что в СССР партийные чиновники испугались быстро растущей популярности этой 
  песни среди широких масс. С конца 1934 г., несмотря на то, что исполнитель песни 
  Утёсов считался придворным певцом кремлёвских палатинов, песня подвергается 
  гонениям, как квинтэссенция буржуазной декаденции. Писатели Ильф и Петров, выполняя 
  заказ партийных идеологов, написали злобный фельетон &quot;У самовара я и моя 
  Маша&quot;. Главрепертком запретил исполнять эту песню в концертах на эстрадах 
  страны и изъял из продажи все оставшиеся утёсовские пластинки с её записями.<br>
  <br>
  Запрет не касался партийных мандаринов, сохранились свидетельства, что в личной 
  фонотеке &quot;кремлёвского горца&quot; эта пластинка была вместе с пластинками 
  Петра Лещенко. Сатирик Григорий Фукс в пьесе о Михаиле Булгакове &quot;Карамболь 
  после полуночи&quot; (журнал &quot;Нева&quot; Nr 9/2006) вкладывает в уста Сталина 
  следующие слова: &quot;Запретный плод сладок. Зощенко нравится, Анна Андреевна, 
  куплетисты Вертинский, Лещенко Петр, Утесов Леонид, (напевает): “У самовара 
  я и моя Маша, вприкуску чай пить будем до утра…”. А мне говорят: “Не наша Маша 
  - не рабоче-крестьянская, мещанская Маша”… А нам нравится... Почему я не хозяин 
  себе? То не так, это не то...&quot;<br>
  <br>
  Леонид Утёсов возобновил довоенную запись песни &quot;У самовара&quot;, выпуская 
  её на долгоиграющей пластинке уже после войны, в 1975 г. На этикетке пластинки 
  появилось новое имя автора песни: Василий Лебедев-Кумач (вместо Фанны Гордон, 
  живущей в Ленинграде репатриантки из Польши, музыканта, композитора и писательницы 
  Феофании Марковны Квятковской). Известно, что Лебедев-Кумач посвятил свой поэтический 
  талант службe партийной идеологии, чего в последние годы жизни очень стыдился. 
  Но обвинять его в плагиате несправедливо, послевоенная пластинка Утёсова была 
  выпущена уже много лет после смерти поэта-песенника. А на московской пластинке 
  1934 года имени Лебедева-Кумача, как автора песни, не было. Я этой пластинки 
  не видел, но так утверждает исследователь эстрады писатель Глеб Анатольевич 
  Скороходов. (Всем, кто интересуeтся этой темой советую познакомится с его работой: 
  <a href="http://www.litportal.ru/genre215/author8390/read/page/14/book43249.html" target="_blank">&quot;Леонид 
  Утёсов. Друзья и враги&quot;</a>).<br>
  <br>
  Но всё это уже иная история, к репертуару песен Петра Лещенко она отношения 
  не имеет.<br>
  <br>
  У каждой песни своя судьба. Судьба песни &quot;У самовара&quot; удивительна. 
  Песня эта, как и песня &quot;Синий платочек&quot; является символом общности 
  наших славянских культур. Родившаяся почти 80 лет назад, oнa остаётся вечно 
  юной.<br>
  <br>
  <br>
  <strong>&quot;АНДРЮША&quot;<br>
  ПОЛЬСКАЯ ПЕСНЯ С РУССКОЙ ДУШОЙ</strong><br>
  <br>
  Нет оснований утверждать, что в Польше русские песни любят больше, чем в других 
  странах мира, но многовековая связь двух славянских культур и обоснованное историческими 
  обстоятельствами повсеместное знание русского языка способствовали распространению 
  в стране русских песен и романсов eщё в те времена, когда Польша называлась 
  Привисленским Краем и была западной окраиной Российской империи. После Октябрьской 
  революции, когда власть в России захватили большевики, нападение Красной Армии 
  на Польшу в 1920-м году и возникшие идеологические барьеры разорвали существующие 
  раньше культурные связи. По сравнению с соседними европейскими странами русская 
  эмиграция в Польше не была многочисленна, история эстрады русского зарубежья 
  в Польше сохранила не очень много имён исполнителей русских песен и романсов. 
  <br>
  <br>
  Расходились по всей стране пластинки с русскими песнями и романсами живущих 
  в Польше и выступающих с концертами артистов Нины Белич, Ольги Каменской, Анны 
  Карениной, Вари Ласки, Макаровой, Юрия Морфесси, Александра Вертинского, Павла 
  Прокопени или Григория Семёнова. Но особенной популярностью пользовались песни 
  Петра Лещенко, распостраняющиеся на грампластинках, несколько из его песен в 
  переводе на польский язык включили в свой репертуар лучшие польские певцы. И 
  сейчас ещё в цикличной передаче &quot;Проходят года - остаются песни&quot; Польского 
  Радио иногда можно услышать польские варианты песен &quot;Два сердца&quot;, 
  &quot;Жизнь цыганская&quot;, &quot;Марфуша&quot;, &quot;Мрачное воскресенье&quot;, 
  &quot;Мусенька&quot;, &quot;Прощай, мой табор&quot;, &quot;Рюмка водки&quot;,&quot;Синяя 
  рапсодия&quot;, &quot;Спи, моё бедное сердце&quot;, &quot;Старинный вальс&quot; 
  и др.<br>
  <br>
  Не существовал импорт советских пластинок из СССР, но спрос на русские песни 
  был настолько огромный, что польские авторы стали создавать песни с русским 
  колоритом. Признаком их &quot;русскости&quot; обычно были чай, самовар, семечки, 
  водка, балалайка, гармошка, тройка и русские имена лирических героев: Соня, 
  Наташа, Тамара, Катюша, Марфуша, Машка, Гришка, Ванька, Володя, Мишенька или 
  Андрюша. Песни эти сочинялись с польским текстом, но в нотных изданиях и на 
  этикетках грампластинок обязательно значилось: &quot;русское танго&quot;, &quot;русский 
  фокстрот&quot; или &quot;русский вальс&quot;. Несколько подобных польских песен 
  с собственным русским текстом включил в свой репертуар Пётр Лещенко.<br>
  <br>
  Одной из таких песен &quot;с русской душой&quot; является фокстрот &quot;Андрюша&quot;, 
  созданный в начале 30-х годов. Автором музыки был польский музыкант, пианист 
  и композитор Зигмунт Белостоцкий (1897-?). В его творческом наследии было несколько 
  подобных песен &quot;с русской душой&quot;: фокстроты &quot;Наташа&quot; и &quot;Катюша&quot;, 
  вальс &quot;О любви не говори мне&quot; и др. Каждая из этих песен в исполнении 
  Петра Лещенко могла бы стать шедевром, но повезло только &quot;Андрюше&quot;, 
  русский вариант этого зажигательного фокстрота в исполнении легендарного певца 
  получил всемирную известность. Зигмунт Белостоцкий создал более сотни произведений, 
  в основном это были танго и фокстроты, создавал он также музыку к фильмам. Был 
  он музыкальным руководителем в театрах Лодзи и Варшавы. Погиб во время войны 
  при ликвидации Варшавского гетто в 1942 или 43 году.<br>
  <br>
  Польский текст &quot;Андрюши&quot; сочинил Людвик Старский (собственно Калушинер) 
  (1903-1984), поэт, писатель, журналист, литературный руководитель театра-ревю 
  &quot;Пражское Око&quot;, автор более 20 киносценариев. Фокстрот &quot;Андрюша&quot; 
  был записан на пластинках в двух вариантах: полном и сокращённом. Полный вариант 
  был записан только один раз в испонении Тадеуша Фалишевского. Остальные исполнители 
  (Адам Астон, Януш Поплавский и Хор Юранда) взяли из текста песни только припев.<br>
  <br>
