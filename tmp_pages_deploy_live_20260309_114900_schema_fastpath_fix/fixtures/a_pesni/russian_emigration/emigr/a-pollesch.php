  Получив солидное музыкальное образование в консерваториях Варшавы и Вены, Петербурский 
  создал много прекрасных произведений лёгкой музыки и быстро получил мировое 
  признание, почти вся жизнь eго прошла вдали от родины: Австрия, СССР, Иран, 
  Палестина, Египет, Бразилия, Аргентина. После гибели жены (Марии Минковской), 
  случившейся во время землетрясения в 1967 году, он перебрался к дочери в Венесуэлу, 
  а в 1968 году вернулся в Польшу, всего за год до &quot;окончательного решения 
  еврейского вопроса в Польше&quot;. B 1969 году Польшу захлестнула мутная волна 
  антисемитизма, большинство евреев, уцелевших во время войны, были изгнаны из 
  страны. В Варшаве 72-летний композитор создал новую семью, женой стала оперная 
  певица Силвия Клейдиш, в 1969-м году у молодожёнов родился сын Ежи.<br>
  <br>
