  niedziela&quot;). В 1932 году в СССР была выпущена пластинка &quot;Польское 
  танго&quot;, это было инструментальное исполнение танго Петербурского &quot;Уже 
  никогда&quot; (&quot;Już nigdy&quot;).<br>
  <br>
