  назывался Санкт-Петербург (без «с» посередине), так что, возможно, никакой связи 
  вообще не существует.&quot; Heт, cуществует, ведь у поляков город на Неве именуется 
  &quot;Пэтэрсбург&quot; (&quot;Petersburg&quot;), поэтому допустимы две русскиe 
  формы фамилии: Петербурский или Петерсбурский. Почему Мелодиста стал Петербургским 
  мне тоже не понятно, но это не псевдоним, ибо братья тоже были Петербургскими. 
  По-видимому фамилию в семье изменили, чтобы избежать путаницы из-за обилия Мелодистов 
  на польской эстраде.<br>
