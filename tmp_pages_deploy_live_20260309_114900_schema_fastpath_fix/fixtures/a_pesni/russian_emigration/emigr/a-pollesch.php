  <br>
  Все записи &quot;Андрюши&quot; в исполнении Петра Лещенко, которые мне удалось 
  прослушать, не имели первой вступительной строфы. Фокстрот этот Петр Лещенко 
  записал на пластинки 3 раза: в Вене в 1933 году, в Риге в 1935 году и в Бухаресте 
  в 1947 году. Звучал &quot;Андрюша&quot; по всему миру, проник контрабандой в 
  &quot;страну всеобщего счастья&quot;. Слушали &quot;Андрюшу&quot; в СССР от 
  Бреста до Владивостока, и сейчас уже трудно понять, чем была опасна эта жизнерадостная 
  песня для партийной идеологии.<br>
  <br>
  В 1938 году появился в СССР и другой &quot;Андрюша&quot;. Это был благонадёжный 
  комсомольский фокстрот, мелодию которого сочинил композитор Илья Жак на стихи 
  поэта Григория Гридова. Пела &quot;Андрюшу&quot; и записала на пластинки юная 
  Клавдия Шульженко. Мне не известна история создания этого фокстрота, в нескольких 
  источниках утверждается: музыка народная, обработка И. Жака. Так вот и этого 
  &quot;Андрюшу&quot; некоторые авторы приписывали Петру Лещенко. Вот курьёзный 
  пример из рассказа Михаила Рощина &quot;Таня Бобрыкина и парад победы&quot;: 
  &quot;Громко играл открытый патефон и так же громко радио, только по радио пели 
  военные песни и марши, а с пластинки несся голос Лещенко: “Эх, Андрюша, нам 
  ли быть в печали, пой, играй, гармонь, на все лады...”.(Михаил Рощин два раза 
  напутал, надо было цитировать точнее : &quot;Не прячь гармонь, играй на все 
  лады&quot;.) В стране, &quot;где жить стало лучше, жить стало веселее&quot;, 
  песня эта была благосклонно отмечена партийной печатью, как способствующая коммунистическому 
  воспитанию молодёжи. Но кажется мне, что без &quot;буржуазного&quot; фокстрота 
  Петра Лещенко не могло бы быть &quot;Андрюши&quot; Жака и Гридова.<br>
  <br>
  <br>
  <strong>&quot;ГАРМОШКА&quot; <br>
  ЕЩЁ ОДНА ПОЛЬСКАЯ ПЕСНЯ С &quot;РУССКОЙ ДУШОЙ&quot;</strong><br>
  <br>
  Русский фокстрот &quot;Гармошка&quot; из репертуара Петра Лещенко родился на 
  польской земле. Об этой песне, которую сочинил выдающийся польский поэт Владислав 
  Шленгель на мелодию Леона Леского, сохранилось очень мало сведений. Известно, 
  что эта песня была создана в 1937 году и в этом же году напел её на грампластинки 
  в сопровождении оркестра п/у Eжи Герда в студии звукозаписи фирмы &quot;Одеон&quot; 
  один из наиболее популярных польских певцов, солист Варшавской Оперы Януш Поплавский 
  (1898-1971). <br>
  <br>
  Песня, видимо, пользовалась большой популярностью, ибо в 1938 году фирма &quot;Одеон&quot; 
  выпустила пластинки с новой записью &quot;Гармошки&quot;. Исполнителем песни 
  на этикетках пластинок был указан Марьян Ольшевский. Это был псевдоним Януша 
  Поплавского, использованный им всего для нескольких песен. (Был у певца ещё 
  один псевдоним - Ян Болеста.) Польские записи &quot;Гармошки&quot; в фирме &quot;Одеон&quot; 
  1937-38 гг. были единственными, которые мне встретились. На форуме сайта &quot;Ностальгия&quot; 
  можно найти следующее утверждение: &quot;Польский первоисточник жанра &quot;российский 
  фокстрот&quot; на музыку Леона Лески и слова Владислава Шленгеля ... пели многие 
  певцы.&quot; Кто? Жаль, что автор не написал об этом подробнее. В каталогах 
  крупнейшей в Польше фирмы звукозаписи &quot;Сирена Электро&quot; фокстрот &quot;Гармошка&quot; 
  не упоминается ни разу.<br>
  <br>
  Трудно найти сведения о авторе музыки этой песни. Кто он - Леон Леский? На этот 
  вопрос я долго не мог найти ответа. Даже в биографических томах самой подробной 
  &quot;Энциклопедии музыки&quot; Польского Научного Издательства нет композитора 
  с этим именем. Нет его и в доступных мне биографических словарях. А если Леский 
  - это не фамилия автора мелодии &quot;Гармошки&quot;, а псевдоним? Следуя этой 
  тропой я установил, что псевдоним &quot;Леский&quot; имел польский композитор 
  Леон Жевуский (1902-1964). Под псевдонимом &quot;Леский&quot;, взятым от названия 
  маленького старинного городка Леско, в 30-х годах прошлого века он сочинил и 
  опубликовал несколько эстрадных песен, из которых наиболее популярными были 
  фокстроты &quot;Гармошка&quot; и &quot;Венгерская капелла&quot;. Его музыкальный 
  талант не расскрылся полностью, ибо он, юрист по образованию, был профессиональным 
  военным, прапорщиком в кавалерии Польского Войска, музыкальным творчеством занимался 
  в свободное от службы время. В 1939 г. Леон Жевуский руководил военным оркестром, 
  в начале войны попал в плен, в немецком лагере для военнопленных создал лагерный 
  театр-ревю &quot;Под папиросой&quot;, сочинил в неволе популярный и сегодня 
  в Польше &quot;Варшавский вальсок&quot;.<br>
  <br>
  Автор &quot;Гармошки&quot; поэт Владислав Шленгель (1914-1943) оставил яркий 
  след в истории польской культуры. &quot;Гармошка&quot; - это одно из ранних 
  произведений поэта. На музыку различных композиторов в 30-х годах создал он 
  несколько десятков текстов популярных эстрадных песен. Из них &quot;Гармошка 
  &quot; - это единственная в его творчестве песня с подзаголовком &quot;русский 
  фокстрот&quot;. Шленгель во время оккупации был летописцем варшавского гетто, 
  вёл &quot;Живой дневник&quot; в кафе &quot;Искусство&quot;, в 1942-43 гг. поэт 
  создал множество волнующих стихотворений о трагедии еврейского народа. Часть 
  из них сохранилась, они были собраны после войны в сборнике &quot;То, что я 
  читал умершим&quot;. Автор погиб с оружием в руках во время подавления восстания 
  в гетто. Это не была борьба за спасение, но борьба за право достойно умереть.<br>
  <br>
  Текст &quot;Гармошки&quot; Шленгель написал в 1937 году, русский колорит песне 
  придают не только имя лирического героя - Володя, не только чай (а не хэрбата), 
  семечки (а не &quot;пэстки&quot;), но даже название песни имеет русский характер: 
  &quot;гармошка&quot; а не &quot;хармошка&quot;. Польский вариант &quot;Гармошки&quot; 
  можно послушать в Сети на многих сайтах.<br>
