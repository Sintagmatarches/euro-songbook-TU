  СССР и Германией, за два года композитор оставил яркий след на советской эстраде, 
  концерты сколоченного на скорую руку из польских беженцев-музыкантов &quot;Голубого 
  джаза&quot; Ежи Петербурского и Генриха Гольда пользовались большим успехом. 
  Популярными стали созданные Петербурским в СССР вальс &quot;Синий платочек&quot; 
  и песня &quot;Маленькое счастье&quot;. Обе эти песни имели польские варианты.<br>
