  братом Генриха Гольда и двоюродным братом Ежи Петербурского. По матери происходил 
  из знаменитого клезмерского рода Мелодистов. После окончания музыкальной школы 
  в Варшаве он перебрался в Лондон, где получил солидное музыкальное образование 
  по класу скрипки и композиции. Вернувшись в Варшаву, вместе с Ежи Петербурским 
  создал танцевальный оркестр, быстро ставший самым лучшим, самым популярным из 
  всех польских оркестров того времени. Из многочисленных произведений композитора 
  самими любимыми были танго &quot;Осенние розы&quot;, &quot;Чёрные глаза&quot; 
  и, особенно, &quot;русские&quot; фокстроты &quot;Ах, те цыганки&quot; и &quot;Наташа 
  танцует&quot;. В годы оккупации Артур Гольд создал в Варшавском Гетто новый 
  оркестр, с которым выступал с концертами в кафе &quot;Современное.&quot; В 42-м 
  году вместе со своим оркестром Гольд был вывезен немцами в лагерь смерти &quot;Треблинка&quot;. 
