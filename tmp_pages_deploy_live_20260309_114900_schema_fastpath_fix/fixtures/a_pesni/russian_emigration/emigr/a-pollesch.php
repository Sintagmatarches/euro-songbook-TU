  и украинских охранников. Музыканты были одеты в разноцветные клоунские фраки 
  с бутафорными галстуками-бабочками на груди. Когда по &quot;дороге смерти&quot; 
  в газовые камеры направлялись очередные тысячи евреев, по приказу немцев музыканты 
