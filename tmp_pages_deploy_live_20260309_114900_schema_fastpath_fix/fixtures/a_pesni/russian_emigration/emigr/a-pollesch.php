  стихотворение &quot;Wielka Teodora&quot; с русским эпиграфом: &quot;Большая 
  Федора - но дура&quot;. Предвидел в нём, что над хаосом, вызванным большевистским 
  переворотом в России, неизбежно возникнет новый деспот, &quot;хам грядущий&quot; 
  более жестокий, чем Иван Грозный или Пётр I, и новому идолу поклоняться будут. 
  Когда после эмиграции военного периода Тувим вернулся в Польшу, новая власть 
  на руках его носила. Гениальный поэт с нами! А он писал верноподданнические 
  оды в честь Великого и Мудрого Вождя, появление которого предвидел в юности. 
  У Тувима были основания опасаться. В 1938 году, потрясённый гибелью многих тысяч 
  невинных жертв сталинского террора, он написал обличительное стихотворение &quot;К 
  московским властителям&quot;, которое могла и припомнить ему партийная инквизиция. 
  Принял близко к сердцу девиз своих советских собратьев по перу, что &quot;лучше 
  есть белый хлеб у Чёрного моря, чем наоборот&quot;. Потом понял, что совесть 
  вся в пятнах, трудно смыть. Но, используя свой авторитет у власть держащих, 
  сумел спасти жизнь нескольким польским патриотам, получившим смертный приговор. 
  Когда умер, похоронили его с почестями в Алее Заслужeнных на военном кладбище...<br>
