В 

  1949 году, находясь на гастролях в Ковне, Вертинский встретился с сестрой Рахили 
  Идой. Рассказала, что она и дочь чудом пережили войну, Идa сама 6 раз была под 
  расстрелом, а мужа немцы расстреляли. Её дочери Лии удалось счастливо бежать, 
  когда большую группу евреев вели на смерть. Из двенадцати бежавших конвоиры 
  застрелили десять.<br>
  <br>
  О дальнейшей судьбе Рахили Яковлевны в польских и русских источниках мне не 
  удалось найти абсолютно никаких сведений. Возможно, что осталась жить в русской 
  колонии Шанхая.<br>
  <br>
  В Польше Александр Вертинский создал ряд песен, например «Мадам, уже падают 
  листья», «В синем и далёком океане», «Злые духи» и знаменитую неувядаемую песню 
  «Пани Ирена», ставшую «визитной карточкой» Вертинского:<br>
  <br>
  <em>Я безумно боюсь золотистого плена <br>
  Ваших медно-змеиных волос, <br>
  Я влюблен в Ваше тонкое имя «Ирена» <br>
  И в следы Ваших слез.<br>
  <br>
  Я влюблен в Ваши гордые польские руки,<br>
  В эту кровь голубых королей,<br>
  В эту бледность лица, до восторга, до муки<br>
  Обожженного песней моей.<br>
  <br>
  Разве можно забыть эти детские плечи,<br>
  Этот горький, заплаканный рот,<br>
  И акцент Вашей польской изысканной речи,<br>
  И ресниц утомленных полет?<br>
  <br>
  А крылатые брови? А лоб Беатриче?<br>
  А весна в повороте лица?..<br>
  О, как трудно любить в этом мире приличий,<br>
  О, как больно любить без конца!<br>
  <br>
  И бледнеть, и терпеть, и не сметь увлекаться,<br>
  И, зажав свое сердце в руке,<br>
  Осторожно уйти, навсегда отказаться<br>
  И еще улыбаться в тоске.<br>
  <br>
  Не могу, не хочу, наконец - не желаю!<br>
  И, приветствуя радостный плен,<br>
  Я со сцены Вам сердце, как мячик, бросаю.<br>
  Ну, ловите, принцесса Ирен!<br>
  </em><br>
  Кто она, гордая полька, пленившая сердце лирического героя песни? Предположения 
  были разные. В телевизионном представлении «Пани Ирена», сюжетом которого было 
  пребывание Вертинского в Польше, есть такая сценка: в гостиничный номер к Вертинскому 
  входит импресарио Фигельман и говорит о необходимости создания песни, посвященной 
  польской женщине. Вертинский соглашается и задумчиво ходит по комнате, напевая: 
  «Я безумно боюсь золотистого плена... Хэлена,.. Магдалена ...нет, не так! Ирена!» 
  Автор сценария даёт понять, что песня не была посвящена какой-нибудь конкретной 
  персоне. Некоторые исследователи уверяют, что Вертинский посвятил песню своей 
  жене Рахили-Ирене. Загадку разгадал польский певец из Кракова Мечислав Свентицкий, 
  который в 1970 году выпустил долгоиграющую пластинку &quot;Жёлтый Ангел&quot; 
  с песнями Александра Вертинского в собственном исполнении. На конверте пластинки 
  было напечатано письмо к воображаемой мадам Ирен, адресованное в никуда. И через 
  некоторое время пришёл ответ из далёкой Америки. Оказалось, что «принцесса Ирен» 
  - это конкретная женщина, в которую когда-то безумно влюбился молодой Вертинский. 
  Однажды в варшавском кинотеатре «Дворец» («Palac») на улице Хмельной (Chmielna) 
  он встретил прекрасную незнакомку, одетую в платье яснопепельного цвета и с 
  тех пор потерял покой. Была это баронесса Ирена Кречковская. Её неземная красота 
  покорила сердце артиста.<br>
  <br>
  Умерла пани Ирена в американском городе Детройт (Detroit) в 1971 году. Это ей 
  «чёрный Пьеро» бросал со сцены своё сердце, как мячик. Интересно, что этот эпизод 
  нашёл своё отражение в фильме «Любовь всё тебе простит» о судьбе певицы Ханки 
  Ордонувны. В фильме молодой артист Томаш Штокингер, играющий роль Вертинского, 
  исполнял на театральной сцене песню «Мадам Ирен» со страшным польским акцентом. 
  Пани Ирена сидела в первом ряду.<br>
  <br>
  Я не знаю, почему в некоторых сборниках текст этой песни сопровождаются посвящением: 
  &quot;Н - й&quot;?<br>
  <br>
  Гнетущая тоска по Родине не даёт Вертинскому покоя. В посольстве СССР он зондирует 
  возможность возвращения. Знакомится с полпредом СССР Петром Лазаревичем Войковым. 
  Был это высокий, довольно неприятный тип с постоянно мутными от пьянства глазами. 
  Говорят, что наиболее употребительным глаголом в речи полпреда был глагол «расстрелять». 
  Не знал Вертинский, что Войков являлся главным организатором и одним из исполнителей 
  зверского убийства всех членов императорской семьи Романовых.<br>
  <br>
  Войков был циничен: «Почему, собственно, Вы не возвращаетесь на родину? Мы постараемся 
  Вам помочь в этом». Даже не намеревался помогать. А 7-го июня 1927 года на вокзале 
  &quot;Warszawa Główna&quot; застрелил его русский патриот Коверда.<br>
  <br>
  Вертинский написал заявление и заполнил соответствующие анкеты. Получил отказ. 
  «Родина простит вам, но это надо заслужить». В переводе с языка дипломатии это 
  значило, что всесильная власть, узурпировавшая право говорить от имени страны, 
  могла сделать с Вертинским всё, позволить вернуться домой, чтобы использовать 
  его талант на службе идеологии или навсегда лишить возможности увидеть «российскую 
  горькую землю», что осталась на том берегу.<br>
  <br>
  Нам теперь трудно понять, почему дух бродяжничества принудил артиста оставить 
  гостеприимную Польшу, чтобы вновь и вновь «проплывать океаны, бороздить материки». 
  В 1927 году Александр Вертинский перебрался во Францию, которая на несколько 
  лет стала, по его словам, «его второй матерью». Впереди на других материках 
  ждали его Америка и Китай. С Польшей пришлось расстаться уже навсегда.<br>
