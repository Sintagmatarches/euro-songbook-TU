  <br>
	<span class="header"><strong>АЛЕКСАНДР ВЕРТИНСКИЙ В ПОЛЬШЕ</strong><br></span>
  <br>
  <em>Первая публикация - <a href="http://www.shanson.org" target="_blank">Шансон – Портал</a>, 6 января 2010 года.</em><br>
<br>
