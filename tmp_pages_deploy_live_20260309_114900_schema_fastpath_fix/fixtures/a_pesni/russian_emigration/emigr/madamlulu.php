Ей шепчутъ страстно и знойно.<br>
Она молчитъ, лишь взоръ скользитъ<br>
Такъ ровнодушно, спокойно.<br>
<br>
Влюбленный видъ ее см<span style="font-family: Palatino Linotype">&#1123</span>шитъ.<br>
Она забавы лишь хочетъ.<br>
Madame Lulu въ своемъ углу<br>
Въ chaizlongu<span style="font-family: Palatino Linotype">&#1123</span> звонко хохочетъ.<br>
<br>
<div style="margin-left:32px">Ей графъ съ утра фiалки предлагаетъ.<br>
Онъ знаетъ что фiалки  вкусъ &quot;madame&quot;.<br>
Le cher baron ей розы присылаетъ<br>
Съ письмомъ о томъ, что будетъ самъ.<br>
<br>
А къ чаю бонбоньерку князь подноситъ<br>
И каждый пальчикъ хочетъ ц<span style="font-family: Palatino Linotype">&#1123</span>ловать.<br>
Madame Lulu пощады проситъ.<br>
Вс<span style="font-family: Palatino Linotype">&#1123</span>хъ гонитъ прочь &quot;пора ей спать.&quot;<br>
<br>
