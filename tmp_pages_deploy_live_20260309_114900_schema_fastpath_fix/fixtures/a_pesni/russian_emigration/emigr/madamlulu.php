Madame Lulu boulevard de France.<br>
<br>
Ее изящной тайной окружаетъ,<br>
Шуршанье шолка ласковый угаръ.<br>
Что скрыто имъ кто угадаетъ,<br>
Тотъ знаетъ тайну женскихъ чаръ.<br>
<br>
Ей запахъ амбры такъ подходитъ,<br>
Ея движенья рай сулятъ.<br>
Madame Lulu съ ума вс<span style="font-family: Palatino Linotype">&#1123</span>хъ сводитъ,<br>
