Приходитъ юный музыкантъ.<br>
Madame Lulu въ восторгъ приводитъ<br>
Его чарующiй талантъ.<br>
<br>
Madame Lulu тебя люблю<br>
Ей н<span style="font-family: Palatino Linotype">&#1123</span>жно онъ нап<span style="font-family: Palatino Linotype">&#1123</span>ваетъ.<br>
