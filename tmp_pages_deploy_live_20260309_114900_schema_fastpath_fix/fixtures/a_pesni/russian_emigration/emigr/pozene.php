	<span class="header"><strong>ПЕСЕНКА 
О ЖЕНЕ </strong><br></span>
<br>
Слова и музыка Александра Вертинского<br>
<br>
Надоело в песнях душу разбазаривать,<br>
И с концертов возвратясь к себе домой,<br>
Так приятно вечерами разговаривать<br>
С своей умненькой, веселенькой женой.<br>
И сказать с улыбкой нежной, незаученной:<br>
- Ах ты, чижик мой, бесхвостый и смешной,<br>
Ничего, что я усталый и замученный,<br>
И немножко сумасшедший и больной.<br>
<br>
<em>Припев:</em><br>
<br>
Ты не плачь, не плачь, моя красавица,<br>
Ты не плачь, женулечка-жена,<br>
В нашей жизни многое не нравится,<br>
Но зато в ней столько раз весна!<br>
<br>
Чтоб терпеть мои актерские наклонности,<br>
Нужно ангельским терпеньем обладать,<br>
А прощать мои дежурные влюбленности –<br>
Это тоже надо что-то понимать.<br>
И, целуя ей затылочек подстриженный,<br>
Чтоб вину свою загладить и замять,<br>
Моментально притворяешься обиженным,<br>
Начиная потихоньку напевать:<br>
<br>
<em>Припев:</em><br>
<br>
Ты не плачь, не плачь, моя красавица,<br>
Ты не злись, женулечка-жена,<br>
В нашей жизни всё еще поправится,<br>
В нашей жизни столько раз весна.<br>
<br>
А потом пройдут года, и вами брошенный,<br>
Постаревший, жалкий и смешной,<br>
Никому уже не нужный и изношенный<br>
Я, как прежде, возвращусь к себе домой<br>
И скажу с улыбкой жалкой и заученной:<br>
- Здравствуй, чиженька, единственный и мой,<br>
Ничего, что я усталый и замученный,<br>
Одинокий, позабытый и больной.<br>
<br>
<em>Припев:</em><br>
<br>
Ты не плачь, не плачь, моя красавица,<br>
Ты не плачь, женулечка-жена,<br>
Наша жизнь уж больше не поправится,<br>
Но зато ведь в ней была весна!<br>
