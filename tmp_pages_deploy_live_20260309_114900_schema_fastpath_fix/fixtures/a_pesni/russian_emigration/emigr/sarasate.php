фирма &quot;Сирена Электро&quot;, Варшава, 1932-33 гг., 22867.<br>
<br>
<br>
 По объяснениям самого Вертинского, в Черновцах, которые тогда принадлежали Румынии (сегодня находятся на Украине), он слушал игру ресторанного скрипача Владеску - короля цыганского жанра. Его женой была известная актриса Сильвия Тоска, которая из-за любви к нему бросила сцену, а Владеску обращался с ней, как тиран. Это произвело на Вертинского сильное впечатление, он написал песню &quot;Концерт Сарасате&quot; и потом в Берлине исполнил ее  перед самим Владеску. Скрипач, по словам Вертинского, не знал, куда бежать, не мог подняться со своего места, а после концерта явился за кулисы с намерением поколотить обидчика, но под влиянием момента разрыдался и раскаялся. Этот эпизод прибавил песне популярности, ее просили на многих концертах.<br>
