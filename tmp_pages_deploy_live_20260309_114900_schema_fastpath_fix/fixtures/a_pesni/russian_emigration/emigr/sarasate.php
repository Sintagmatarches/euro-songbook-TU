<br>
Слова и музыка Александра Вертинского<br>
<br>
Ваш любовник скрипач. Он седой и горбатый,<br>
Он вас дико ревнует, не любит и бьет,<br>
Но, когда он играет концерт Сарасате,<br>
Ваше сердце, как птица, летит и поет.<br>
<br>
Он альфонс по призванью. Он знает секреты<br>
И умеет из женщины сделать зеро,<br>
Но, когда затоскуют его флажолеты,<br>
Он божественный принц, он влюбленный Пьеро…<br>
<br>
Он вас скомкал, сломал, обокрал, обезличил.<br>
Фам де люкс* он сумел превратить в фам де шамбр.*<br>
А-ха, а-ха. <br>
И давно уж не моден, давно не приличен<br>
Ваш кротовый жакет с легким запахом амбр.<br>
<br>
И в усталом лице, и в манере держаться<br>
Появилась у вас и небрежность, и лень.<br>
А-ха, а-ха. <br>
Разве можно так горько, так зло насмехаться,<br>
Разве можно топтать каблуками сирень?..<br>
<br>
И когда вы, страдая от ласк хамоватых,<br>
Тихо плачете где-то в углу, не дыша,<br>
Он играет для вас свой концерт Сарасате,<br>
От которого кровью зальется душа!<br>
<br>
Безобразной, ненужной, больной и брюхатой,<br>
Ненавидя его, презирая себя,<br>
Вы прощаете все за концерт Сарасате,<br>
Исступленно, безумно и больно любя!<br>
<br>
