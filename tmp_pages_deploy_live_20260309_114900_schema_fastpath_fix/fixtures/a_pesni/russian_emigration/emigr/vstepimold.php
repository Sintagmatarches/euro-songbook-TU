  фирма &quot;Сирена Электро&quot; Варшава, 1932-33 гг., 22886 (&quot;Стерь молдаванская&quot;); 
  Апрелевский завод, Москва, 1944 г., 11872.<br>
  <br>
  <br>
  В ноябре 1943 году Вертинский возвращается из Шанхая в Россию. 21, 24 и 26 января 
  1944 года в Москве в Доме звукозаписи он записывает 15 песен для пластинок Апрелевского 
  завода. Это единственная прижизненная серия пластинок Вертинского на родине 
