        Тексты и ноты &quot;российских песен&quot; - ранних русских романсов, появившихся как результат синтеза канта (хоровой песни a capella) и европейских танцевальных песенок для голоса и инструмента. Дорогу &quot;российской песне&quot; открыла поэтическая реформа Тредиаковского и Ломоносова 1730-40-х гг., изменив систему русского стиха с <em>силлабики</em> на <em>силлабо-тонику</em>: теперь требовались не только рифма и равное количество слогов в рифмующихся строках, но и фиксированное место сильных слогов (&quot;иктов&quot;) в этих строках. Например, сильны (желательно ударны) все четные слоги (поэтический метр &quot;ямб&quot;) или все нечетные слоги (метр &quot;хорей&quot;):<br>
        <br>
        <div style="margin-left:32px"><em>Мой д<strong>Я</strong>дя с<strong>А</strong>мых ч<strong>Е</strong>стных пр<strong>А</strong>вил</em> (ямб)<br>
        <br>
        <strong><em>У</em></strong><em>м толь сл<strong>А</strong>бый пл<strong>О</strong>д труд<strong>О</strong>в / кр<strong>А</strong>тки<strong>я</strong> на<strong>У</strong>ки</em> (хорей)</div>
        <br>
        Стихи, построеные по этим принципам, пелись без насилия над русским ударением, так как сильные доли текста и мелодии синхронизировались. Принципиальное содержательное отличие русского романса от традиционных (&quot;тонических&quot;) русских народных песен - эгалитарность: романс не знает гендерного и социального неравенства, исполняться также может как женщиной, так и мужчиной.<br>
