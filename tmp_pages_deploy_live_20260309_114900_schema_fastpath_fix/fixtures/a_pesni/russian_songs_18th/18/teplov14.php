	<h1>В ОТРАДУ ГРУСТИ И МУЧЕНЬЯ</h1>
	<br>
	Музыка Григория Теплова<br>
	<br>
	1. В отраду грусти и мученья,<br>
Что я, мой свое, тобой терплю,<br>
Иного нет мне утешенья,<br>
Лишь только что я слезы лью.<br>
<div class="refren">О, ты, сладчайшая утеха,<br>
Что в свете есть тебя милей?<br>
Но есть-ли ты не дашь успеха,<br>
Ты - самой лютой смерти злей. <em>(2 р.)</em></div>
<br>
2. Все, вижу я, в тебе временно<br>
К смертельной горести моей!<br>
Что ж сердце столь ожесточенно,<br>
Доколе-ж будешь в мысли сей?<br>
<div class="refren">Уже на место ласки - строгость<br>
Являет твой прелестной взгляд,<br>
На место нежности - суровость<br>
Уста твои мне говорят. <em>(2 р.)</em></div>
<br>
3. Ах, долго ль мне тобой терзаться,<br>
Всегда твою холодность зреть,<br>
Грустит и, ах, не утешаться,<br>
В отчаянии умереть?<br>
<div class="refren">Увяну в самом лучшем свете,<br>
Когда угодно то судьбе,<br>
А ты останешься на свете:<br>
Ищи верней меня себе <em>(2 р.)</em></div>
