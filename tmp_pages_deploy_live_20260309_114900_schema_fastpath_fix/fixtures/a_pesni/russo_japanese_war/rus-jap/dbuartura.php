ПЕСНИ</strong> <br></span>
<br>Неизвестный автор<br>
  <br>
  Было дело у Артура — <br>
  Дело скверное, друзья... <br>
  Того, Ноги, Камимура <br>
  Не давали нам житья.<br>
<br>Мы с соседкой желтолицей <br>
  Ей-же-ей сцепились зря... <br>
  «Полечу я вольной птицей <br>
  Да за синие моря»... <br>
<br>Уж давно готовы лодки, <br>
  Мы поедем в край иной... <br>
  «Ну, садись, моя красотка, <br>
  Только рядышком со мной». <br>
<br>Приказали нам от брега <br>
  Удалиться в два часа... <br>
  «Пропадай моя телега, <br>
  Все четыре колеса».<br>
<br>Там стоял «Варяг» железный <br>
  И «Кореец» с ним как раз... <br>
  «Выходи, о друг мой нежный, <br>
  Бил свиданья час»... <br>
<br>Порт-артурцы проглядели, <br>
  Как на нас нашла гроза... <br>
  «Оглянуться не успели, <br>
  Как зима катит в глаза». <br>
<br>Грустно, вяло и несмело <br>
  Рать солдат пустилась в путь — <br>
  «Ноги босы, грязно тело <br>
  И едва прикрыта грудь»... <br>
<br>Куропаткин горделивый <br>
  Прямо в Токио спешил <br>
  «Что ты ржешь, мой конь ретивый, <br>
  Что ты шею опустил?» <br>
<br>Вот уж он на бранном поле, <br>
  Слава северных дружин... <br>
  «Страшно, страшно поневоле <br>
  Средь неведомых равнин». <br>
<br>А наместник уезжает <br>
  Безвозвратно, навсегда .. <br>
  «Птичка божия не знает <br>
  Ни заботы, ни труда». <br>
<br>Куропаткину обидно, <br>
  Что не страшен он врагам... <br>
  «В поле бес нас водит, видно, <br>
  Да кружит по сторонам»... <br>
<br>А Ойяма наступает <br>
  Ночью и при свете дня, <br>
  «Посмотри: вон-вон играет, <br>
  Дует, плюет на меня». <br>
<br>С Порт-Артуром попрощайся, <br>
  Получил большущий нос... <br>
  «Гром победы раздавайся, <br>
  Веселися, храбрый росс».<br>
<br>Ходят пленники, как тени, <br>
  Без отчизны, без семьи... <br>
  «Ах вы, сени мои, сени, <br>
  Сени новые мои». <br>
<br>Генералов вереница, <br>
  Офицеров без числа... <br>
  «Спой мне песню, как синица <br>
  Тихо за морем жила». <br>
<br>Поработал на солдата <br>
  Интендантский хоть куда... <br>
  «Хороши наши ребята, <br>
  Только — славушка худа». <br>
<br>&lt;15 апреля 1906&gt;<br>
