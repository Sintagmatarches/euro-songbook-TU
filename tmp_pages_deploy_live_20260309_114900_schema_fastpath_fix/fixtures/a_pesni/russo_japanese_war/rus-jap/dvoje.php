  13 февраля 1904 г. (по старому стилю) дали бой четырем японским кораблям. 
  &quot;Решительный&quot; прорвался, &quot;Стерегущий&quot; не смог и сражался 
  до последнего человека и снаряда. Все командиры погибли. Из всего экипажа в 
  живых осталось только четверо нижних чинов. По легенде, когда японцы взяли разбитый 
