<h1>КОГДА ПАДАЮТ ЛИСТЬЯ </h1>

Танго на слова Павла Бернадского<br>
<br>
Сухой листвой в саду шуршит печально осень.<br>
  И в окна каплями стучит холодный дождь.<br>
  Сейчас пробьют часы тяжелым звоном восемь,<br>
  Но знаю я, ты не придешь.<br>
Ты не придешь ко мне с улыбкой и цветами<br>
  Осенний долгий вечер коротать.<br>
  Как часто мы, того не зная сами,<br>
  Не можем наше счастье удержать.<br>
<br>
<span class='i'>Припев:<br></span>
<br>
Оно приходит к нам незваным.<br>
  Оно уходит, не спросясь.<br>
  И дорогим воспоминаньем<br>
  Прошедших дней оно терзает нас.<br>
И все, что было так красиво,<br>
  Сметает время злой рукой.<br>
  Но ты за все скажи ему спасибо,<br>
  Оно тебе вернет покой.<br>
<br>
Быть может, там, где ты теперь, метель и вьюга.<br>
  И все тропинки снегом замело.<br>
  Быть может, ты совсем забыл о прежнем друге<br>
  И чувство прежнее прошло.<br>
А здесь шуршит 
  еще сухой листвой ненастье,<br>
  Осенний дождь шумит всю ночь в саду.<br>
  Я берегу свое былое счастье,<br>
  Ты не придешь, но я тебя здесь жду.<br>
  <br>
<span class='i'>Припев.<br></span>
<br>
  <span class='i'>Из репертуара Изабеллы Юрьевой. <br>
  <br>
  Очи черные: Старинный русский романс.  М.: Эксмо, 2004. Подпись: &quot;Танго на сл. П. Бендерского&quot;.<br> </span>
  <br>
  <br>
Запись на пластинку – Москва, 1939, пластинки Ногинского, Апрелевского заводов и Московской фабрики звукозаписи, подпись: &quot;Обр. С. Кагана, слова П. Бернадского&quot;. Прослушать фонограмму можно <a href="http://www.russian-records.com/details.php?image_id=10013" target="_blank">здесь</a>. <br>
