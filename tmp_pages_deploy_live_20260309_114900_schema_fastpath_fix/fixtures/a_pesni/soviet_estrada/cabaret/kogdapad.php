Другое заглавие - &quot;Я жду тебя&quot;. Вероятно, это русский текст на неустановленное пока иностранное танго. Этому же автору слов - Павлу  Бернадскому - принадлежат слова еще одного танго из репертуара Изабеллы Юрьевой - <a href="malschastje.php">&quot;Маленькое счастье&quot;</a> (музыка Ежи Петерсбурского). <br>
