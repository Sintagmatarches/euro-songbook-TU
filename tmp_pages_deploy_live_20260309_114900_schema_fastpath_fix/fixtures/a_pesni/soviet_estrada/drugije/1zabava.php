	<h1>ЗАБАВА </h1>Музыка Сергея Беляева<br>
  Слова Сергея Есенина<br>
  <br>Мне осталась одна забава:<br>
  Пальцы в рот - и веселый свист.<br>
  Прокатилась дурная слава,<br>
  Что похабник я и скандалист.<br><br>Ах! какая смешная потеря!<br>
  Много в жизни смешных потерь.<br>
  Стыдно мне, что я в бога верил.<br>
  Горько мне, что не верю теперь.<br><br>Золотые, далекие дали!<br>
  Все сжигает житейская мреть.<br>
  И похабничал я, и скандалил<br>
  Для того, чтобы ярче гореть.<br><br>Дар поэта – ласкать и карябать,<br>
  Роковая на нем печать.<br>
  Розу белую с черной жабой<br>
  Я хотел на земле повенчать.<br><br>Пусть не сладились, пусть не сбылись<br>
  Эти помыслы розовых дней.<br>
  Но коль черти в душе гнездились –<br>
  Значит, ангелы жили в ней.<br><br>Вот за это веселие мути,<br>
  Отправляясь с ней в край иной,<br>
  Я хочу при последней минуте<br>
  Попросить тех, кто будет со мной, -<br><br>Чтоб за все за грехи мои тяжкие,<br>
  За неверие в благодать<br>
  Положили меня в русской рубашке<br>
  Под иконами умирать.<br>
  <br>
  Слова - 1923<br>
  <br>
  <span class='i'>Журн. «Гостиница для путешествующих в прекрасном», М. 1924, 
  № 1 (3). - под загл. &quot;Мне осталась одна забава&quot;.<br>
  <br>
  Есенин С. Собр. соч. в 3-х т. Т. 1. М.: Правда, 1970. - под загл. &quot;Мне осталась одна забава&quot;.</span><br>
  <br><br>
  На музыку положено Сергеем Беляевым около 1990 г., песня исполнялась Александром Малининым, в 1990 г. вышла на его пластинке &quot;Неприкаянный&quot; (фирма &quot;Мелодия&quot;), была лауреатом &quot;Песни года&quot;. Во 2-м куплете песни отклонение от Есенина: <em>«Стыдно мне, что я в бога <strong>не</strong> верил»</em>. Сергей Беляев (р. 1960) - выпускник ГИТИСа (курс Олега Табакова), артист театра и кино.<br>

