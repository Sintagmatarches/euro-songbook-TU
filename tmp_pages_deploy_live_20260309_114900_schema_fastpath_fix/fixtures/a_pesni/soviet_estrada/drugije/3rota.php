<h1>ШЛА 
С УЧЕНИЙ ТРЕТЬЯ РОТА </h1>
Из кинофильма &quot;Солдат Иван Бровкин&quot; (1955)<br>
<br>
Музыка Анатолия Лепина<br>
Слова Алексея Фатьянова<br>
<br>
Шла с учений третья рота <br>
У деревни на виду. <br>
Мимо сада-огорода, <br>
Мимо девушек в саду. <br>
<br>
Прекратила рота пенье, <br>
Глаз не может оторвать. <br>
Будто был приказ - равненье <br>
Всем на девушек держать. <br>
<br>
Тут одна в платочке синем <br>
Говорит: &quot;Чем пыль толочь <br>
На дороге, рты разиня, <br>
Шли бы девушкам помочь&quot;. <br>
<br>
Старшина был очень краток, <br>
Выполнять приказ изволь - <br>
Прополоть полсотни грядок <br>
К восемнадцати ноль-ноль! <br>
<br>
Этот случай не забылся, <br>
А причина тут одна: <br>
Через месяц вдруг женился <br>
Наш товарищ старшина! <br>
<br>
1955 <br>
<br>
