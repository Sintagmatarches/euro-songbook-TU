  <h1>Я ПЕСНЕ ОТДАЛ ЖИЗНЬ СПОЛНА </h1>
  <span class='i'>Я люблю тебя, жизнь: Песни на все времена / Сост. Л. Сафошкина, В. Сафошкин. 
  М.: Эксмо, 2004. С. 11-26.<br></span>
  <br>
  <br>
  <img src="a-japesne.jpg" alt="Леонид Утесов" width="290" height="401"><br>
  <br>
  <br>
  И в тридцатые, и в сороковые, и в пятидесятые годы не было на просторах огромного 
  Советского Союза уголка, где не знали бы имени Леонида Утесова, не слышали и 
  не любили его песен. Голос Утесова постоянно звучал по радио, популярные утесовские 
  шлягеры миллионами экземпляров тиражировались грамзаписью, на концертах певца 
  никогда не было свободных мест. Не будет преувеличением сказать, что песни, 
  исполняемые Утесовым и его джаз-оркестром, пела вся страна... <br>
  <br>
  ...Леонид Осипович Утесов пришел в джаз уже опытным музыкантом, зрелым эстрадным 
  артистом. Его творческий путь начинался в родной Одессе, а первые шаги на артистическом 
  поприще он сделал в цирке Г. Н. Бородинова, выступая в роли гимнаста на трапеции. 
  <br>
  <br>
  С этим номером он в 1909 году и отправляется в гастрольную поездку по югу Украины, 
  но неожиданная болезнь заставляет воздушного гимнаста Утесова прервать турне. 
  Сам по себе сюрприз не из разряда приятных, но кто знает, как сложилась бы дальнейшая 
  жизнь Утесова, не застрянь он тогда в Кременчуге и не приди после выздоровления 
  в местный театр миниатюр. <br>
  <br>
  Несмотря на скромность и своеобразие сценического опыта, в театре отнеслись 
  с интересом к артистичному, живому одесситу. Особенно много дали молодому циркачу 
  занятия с ведущей артисткой театра А. А. Арендс. С ее помощью и благодаря собственному 
  неиссякаемому трудолюбию Утесов в рекордно короткий срок овладевает необходимыми 
  азами актерского мастерства и вскоре становится в один ряд с лучшими актерами 
  театра. Кременчугская публика провожает его со сцены дружными аплодисментами, 
  и обозреватель местной газеты уже в первый театральный сезон отмечает: "В оперетте 
  Э. Одрана "Невеста-автомат" хороши были г.г. Юрский и Утесов". Со временем молодой 
  артист становится настоящим любимцем публики и даже удостаивается собственного 
  бенефиса... <br>
  <br>
  Бенефис проходит с шумным успехом, и уже в 1912 году Утесов получает приглашение 
  в Одесский театр миниатюр. Большой Ришельевский театр - таково полное название 
  места новой работы Утесова - считался одним из лучших театров миниатюр в тогдашней 
  России. На его сцене играли такие замечательные актеры начала прошлого века, 
  как Е. М. Баскакова, П. Н. Поль, К. Э. Гибшман, Ф. Н. Курихин, дошедший до нас 
  в роли факельщика в "Веселых ребятах", популярный конферансье и режиссер А. 
  Г. Алексеев. Для прошедшего хорошую школу в провинции новичка Большой Ришельевский 
  стал подлинным университетом актерского мастерства... <br>
  <br>
  Одновременно Утесов пробует себя на эстраде. Молодого актера часто видят на 
  выступлениях известных в то время куплетистов С. Сарматова и С. Сокольского, 
  П. Троицкого и Ю. Убейко, И. Гурко и Н. Савоярова, комика-рассказчика В. Хенкина. 
  Утесов настолько увлекается куплетом, что подготавливает собственный номер. 
  Его появление на сцене в роли шустрого одесского газетчика производит большое 
  впечатление на избалованную одесскую публику, и на некоторое время номер становится 
  гвоздем сезона. <br>
  <br>
  Вместе с успехом приходит и столь необходимая каждому актеру уверенность в своих 
  силах, и Утесов вскоре переезжает в Херсон, где в местном театре миниатюр получает 
  ряд ролей первого плана. В передвижном театре "Мозаика", куда актер переходит 
  через некоторое время, Леонид Осипович встречает очаровательную Елену Ленскую, 
  молодую актрису, которой суждено будет пройти с ним по дорогам жизни более сорока 
  лет... <br>
  <br>
  В 1914 году разражается Первая мировая война. По возрасту Утесов подлежит призыву 
  в армию, но врачебная комиссия обнаруживает у него врожденный порок сердца. 
  Демобилизованный Утесов возвращается в Одессу, в труппу Большого Ришельевского... 
  Однако теперь он уже не дебютант, а сложившийся актер, зрелый мастер. Восторженным 
  становится и тон газетных рецензий. Так, в номере от 19 апреля 1915 года газета 
  "Маленькие одесские новости", живописуя утесовский бенефис, пишет буквально 
  следующее: "Театр ломился от публики, артиста вызывали бесконечное количество 
  раз, заставляли бисировать". <br>
  <br>
  Первая попытка Утесова завоевать московскую публику относится к 1917 году и 
  оказывается неудачной. В театре П. П. Струйского на Большой Ордынке "одесского 
  куплетиста" ожидает прохладный прием, и он возвращается обратно в Одессу - впрочем, 
  не особенно унывая по поводу своего "провала". Утесов по-прежнему много работает, 
  пробуя себя в самых разных жанрах и амплуа. Одаренный имитатор и пародист "от 
  Бога", он создает оригинальный номер, в котором сам изображает известных в то 
  время сценических знаменитостей - от трагика Мамонта Дальского до рассказчиков 
  В. Хенкина и Я. Южного. Эстрадный спектакль, названный "От Мамонта Дальского 
  до Марии Ленской", получает горячий прием у знающего толк в юморе одесского 
  зрителя, но с особенным восторгом встречается на сцене появление "комического 
  хора", артисты которого на глазах перевоплощаются то в опереточных героев, то 
  в цыган... <br>
  <br>
  Особенно была любима одесситами интермедия, в которой Утесов с "хористами" изображают 
  выступление симфонического оркестра. Происходило это так. Утесов, играющий дирижера, 
  во фраке и накрахмаленной манишке становится перед почтительно замершим оркестром. 
  Строгим взглядом через монокль окидывает музыкантов, призывным жестом поднимает 
  дирижерскую палочку. Взмахивает рукой и... Зал разражается дружным хохотом. 
  Плохо закрепленная пристежная манжета отрывается от рукава и - летит прямо в 
  зал! Однако невозмутимому маэстро все нипочем, он лишь слегка поворачивает голову 
  и спокойно бросает в на секунду притихший зал: "Подай белье". Зрители буквально 
  валятся от оглушительного, гомерического хохота... Фокус с оторвавшейся манжетой 
  завоевывает такую популярность у публики, что потом включается в выступление 
  "Оркестра имени листа прокатного цеха" в кинофильме "Веселые ребята". А театрализация 
  музыкальных номеров как основной прием ложится в основу будущего утесовского 
  "Теа-джаза". <br>
  <br>
  ...Благодарная Одесса буквально носит на руках любимого артиста, но сам Утесов 
  прекрасно понимает, что новый театр, его будущее делаются сейчас в Москве и 
  Петрограде. Утесов снова едет в Москву и прямо с поезда отправляется в Театр 
  революционной сатиры. Не известно, что сыграло б<strong>о</strong>льшую роль 
  - артистические способности новичка или его наряд демобилизованного солдата: 
  френч, галифе и пара сапог на одну ногу, но Утесов был принят в труппу и с успехом 
  сыграл в ряде пьес "революционного звучания". <br>
  <br>
  Впрочем, долгой разлуки с музыкой и песней артист не вынес и вскоре перебрался 
  в Театр музыкальной комедии, на сцене которого в то время блистали своими талантами 
  И. В. Ильинский и Г. М. Ярон. Несмотря на наличие звезд, театр, однако, топтался 
  на месте, и динамичный одессит вскоре перебирается в Петроград, и, как оказалось, 
  не напрасно. Именно здесь, в петроградском Театре музыкальной комедии, Утесову 
  удается сыграть одну из своих лучших ролей - роль Менелая в "Прекрасной Елене" 
  Ж. Оффенбаха. <br>
  <br>
  А в феврале 1923 года петроградцам посчастливилось стать зрителями беспримерного 
  в истории театрального искусства представления. В качестве его автора, а также 
  исполнителя всех главных ролей выступил один человек - артист Театра музыкальной 
  комедии Леонид Утесов. Театральные афиши, извещавшие о времени и месте спектакля 
  "От трагедии до трапеции", обещали: Утесов покажет вам все. И афиши не обманули 
  - в тот незабываемый вечер Утесов продемонстрировал восхищенным петроградцам 
  весь спектр своего актерского таланта, все грани своего мастерства... <br>
  <br>
  ...Спектакль и начинался с неожиданности - Утесов выходил в костюме и гриме 
  главного героя "Преступления и наказания". Драматическая сцена допроса Раскольникова 
  заставляет зрителей взволнованно притихнуть... Но не успевают отзвучать аплодисменты, 
  как из-за кулис появляется весело пританцовывающий Менелай. Стихают аккорды 
  классической оперетты, и снова на сцене Утесов, теперь уже - конферансье, рассказчик, 
  куплетист... Инструментальное трио - виолончель, рояль, скрипка - исполняет 
  романс М. Глинки "Не искушай", и скрипка в руках Утесова буквально завораживает 
  зрителей... <br>
  <br>
  Снова зал взрывается аплодисментами, но артист, спокойно переждав овации, откладывает 
  в сторону скрипку и берет в руки гитару. Старинные русские и цыганские романсы 
  звучат в устах Утесова так, как будто актер только что вышел из круга цыганского 
  костра... <br>
  <br>
  Восхищение зрителей достигает своего апогея, когда Утесов вкупе с профессиональной 
  балериной М. Пильц демонстрирует целый набор классических "па" и "поддержек". 
  Но программа представления еще далеко не исчерпана - впереди знаменитый "комический 
  хор", пародии, эксцентрические танцы, где в качестве партнеров Утесова выступают 
  лучшие эстрадные танцовщики России... А когда в финале спектакля Утесов проносится 
  под потолком театра на цирковой трапеции, зрители вскакивают с мест и продолжают 
  аплодировать стоя... <br>
  <br>
  "Это был даже не успех, - напишет об этом потом один из зрителей, режиссер Г. 
  Крыжицкий. - Скорее какой-то бешеный фурор, необычная сенсация... Битком набитый 
  театр... Публика неистовствовала... Галерка бесновалась, как в старое доброе 
  время..." <br>
  <br>
  Феерический бенефис Утесова имел к тому же рекордную продолжительность - долгий 
  свой путь от трагедии до трапеции Леонид Осипович проделывал за шесть часов. 
  Но главное, разумеется, было не в этом... Впервые наглядно и ярко в присутствии 
  сотен зрителей было продемонстрировано то, что иначе, как феноменом Утесова, 
  и не назовешь. Действительно, уникальный артистизм Утесова, постоянно реализовывавший 
  себя во многих сопредельных, но все-таки разных областях искусства, не знает 
  себе равных ни до, ни после него. Как певцу и музыканту, ему была доступна стилистика 
  любого жанра - вплоть до запретной тогда блатной песни, которая в его устах, 
  в его мастерском исполнении приобретала звучание жестокого городского романса. 
  Однако популярность "Мурки", "Лимончиков", "С одесского кичмана" сослужит впоследствии 
  плохую службу Леониду Осиповичу, на долгие годы отодвинув его, уже известного 
  всей стране артиста, на задворки начальственного благоволения как "подозрительную 
  личность", певца воровской "малины". А ведь забубенная песенка "С одесского 
  кичмана" была впервые исполнена Утесовым со сцены ленинградского Театра сатиры 
  в спектакле "Республика на колесах", где он сыграл острогротесковую роль главаря 
  анархистов... <br>
  <br>
  ...Моноспектакль "От трагедии до трапеции" еще долгое время вспоминается в театральных 
  колонках ленинградских газет. Авторы отзывов и рецензий называют Утесова уникальным 
  явлением, прочат ему будущее актера-универсала... <br>
  <br>
  Но путь Утесова уже определен. Музыка, песня... Джаз. <br>
  <br>
  Проходит несколько лет, и в Ленинграде на сценической площадке Сада отдыха имени 
  Ф. Дзержинского состоится премьера утесовского "Теа-джаза", первого джазового 
  представления в России. Искрометная темпераментность джазовой музыки, доступность 
  ее языка буквально с первых тактов покоряют массового зрителя... <br>
  <br>
  Не на пустом месте возникает отечественный, утесовский джаз - с его острыми 
  степовыми ритмами, изобразительностью музыкального гротеска. Мелодические решения 
  подсказываются народной песней, романсом. А поездка за границу и знакомство 
  с лучшими образцами джазовой классики избавляют Утесова от надобности "изобретать 
  велосипед"... <br>
  <br>
  В конце двадцатых годов лед недоверия к легкому жанру трогается и в столице. 
  Возводится московский мюзик-холл, на открытие которого приглашается джаз-оркестр 
  Леонида Утесова. Под сводами здания на Триумфальной площади впервые звучат мелодии 
  лучших отечественных и зарубежных джазовых композиторов... А в финале представления 
  и артисты, и зрители становятся участниками своеобразного карнавального шествия. 
  Джаз-оркестр, напевая и наигрывая "Пока, пока, уж ночь недалека", в полном составе 
  спускается со сцены в зал и через проход между креслами направляется к выходу, 
  а затем и на улицу. Там аплодирующих зрителей ожидает еще один сюрприз - на 
  фронтоне здания, превращенного в большой киноэкран, снова возникает лицо Утесова, 
  который под восторженные крики толпы завершает свое прощальное "Пока, пока...". 
  <br>
  <br>
  Толпа народа у театрального подъезда, горящие в вечернем небе крупные огненные 
  буквы "Мюзик-холл" невольно заставляют вспомнить кадры из популярного фильма 
  Г. Александрова "Веселые ребята". Кстати, роль двойника Кости Потехина - иностранного 
  дирижера Коста Фраскини сыграл в этом фильме А. Арнольд, талантливый режиссер, 
  который вместе с Утесовым готовил и осуществлял постановочную часть представления 
  на Триумфальной... <br>
  <br>
  Остроумный трюк с поющим на стене Утесовым оказался своеобразной кинопробой 
  артиста на роль Кости Потехина. Впрочем, некоторый опыт работы в кино у Утесова 
  уже имелся - в 1917 году, вернувшись из Москвы в Одессу, он "мимоходом" снялся 
  в революционном фильме "Лейтенант Шмидт". И вот спустя почти семнадцать лет 
  Утесов снова появляется на экране - на этот раз уже в главной роли... <br>
  <br>
  Современному телезрителю, знакомому с "Веселыми ребятами" по ретроспективным 
  показам классики кино, трудно представить себе тот восторг, с которым кинозрители 
  30-х годов встречали этот фильм. Переполненные кинозалы, огромные очереди за 
  билетами сопровождали появление афиш с улыбающимся парнем в широкополой шляпе 
  и длинным пастушеским кнутом в руке. Десятки миллионов людей повторяли шутки 
  Кости Потехина, пели его песни... <br>
  <br>
  Поистине звездную славу снискала себе "поющая актриса" Любовь Орлова. Всесоюзное 
  радио не переставало транслировать мелодии из фильма. Музыка, песни, джаз по 
  праву делили с создателями Н. Эрдманом и В. Массом, композитором И. Дунаевским, 
  поэтом В. Лебедевым-Кумачом шумный успех "Веселых ребят". <br>
  <br>
  ... История создания "Пастуха из Абрау" - так сначала назывался фильм - уникальна 
  и в то же время типична для своего времени. <br>
  <br>
  В тридцатые годы начинается сотрудничество джаз-оркестра Утесова с молодым Исааком 
  Дунаевским. Мелодичность, выразительность его музыки, богатство интонаций - 
  от мягкого лиризма до бодрого мажора - привлекают к себе Утесова. Близки первопроходцам 
  джаза и те новации, которые вносит Дунаевский в эстрадную музыку тридцатых. 
  В совместном поиске не только обновляется репертуар популярного музыкального 
  коллектива, но и нащупываются пути, которыми предстоит идти отечественной эстрадной 
  музыке. И вот в 1933 году на суд взыскательного ленинградского зрителя представляется 
  новая программа джаз-оркестра "Музыкальный магазин". Зритель валом валит в "Музыкальный 
  магазин" Утесова и Дунаевского, но самих авторов, естественно, не может не волновать, 
  как к их поискам отнесутся "наверху"... Слишком уж свежи в памяти гонения на 
  джаз - "музыку толстых" - в двадцатые годы, да и газетно-журнальная травля, 
  затеянная идеологическими инквизиторами от культуры, все еще не стихает... "Пропаганда 
  фокстрота в мюзик-холле"... "Джазбандисты превращаются в бандитов"... Афоризмами 
  подобного рода буквально пестрят печатные страницы даже центральных газет... 
  <br>
  <br>
  Вопреки ожиданиям, новая программа утесовского джаза получает одобрение. Побывавший 
  на концерте заместитель председателя Комитета по делам искусств при Совете Народных 
  комиссаров Б. Шумяцкий оказывается умным и дальновидным чиновником. Почувствовав 
  перспективность того, что делают ленинградцы, он не только благословляет новую 
  программу, но и предлагает снять по "Музыкальному магазину" художественный фильм. 
  <br>
  <br>
  И время показало, насколько чутье не обмануло Б. Шумяцкого. Одним из первых 
  "Пастуха из Абрау" посмотрел Максим Горький. Он, по его словам, обхохотался 
  и в одном из разговоров со Сталиным предложил устроить просмотр. Вождь согласился 
  и, судя по его отзыву, остался доволен. <br>
  <br>
  "...Ошеломляющим был успех фильма на Западе, - вспоминает Леонид Осипович в 
  своей книге мемуаров. - До "Веселых ребят" американцы знали Россию Достоевского. 
  Теперь они увидели большие перемены в психологии людей. Люди смеются..." <br>
  <br>
  Да, подлинный юмор, настоящая музыка не знают границ. И Америка, избалованная 
  высококлассной голливудской кинопродукцией, радовалась и горевала вместе с простодушными 
  героями александровского фильма. Не случайно в США фильм шел под названием "Москва 
  смеется", а на престижном венецианском фестивале был удостоен премии. Сам Чарли 
  Чаплин с похвалой отозвался о первой советской музыкальной комедии, сказав: 
  "Это большая победа. Это агитирует больше, чем доказательство стрельбой и речами". 
  <br>
  <br>
  И только на родине создателей фильма победа "Веселых ребят" была воспринята 
  неоднозначно. Такой безусловный успех кое-кого не радовал, а настораживал - 
  действительно, уж не являются ли все эти отзывы, премии и прочие знаки внимания 
  "уловками буржуазной идеологии", пропагандирующей одно из своих проявлений - 
  джаз? <br>
  <br>
  "Получив "Правду" и "Известия", - вспоминает далее Леонид Осипович, - я с интересом 
  стал читать большие статьи, посвященные "Веселым ребятам", и не мог не удивиться. 
  В обеих статьях были указаны фамилии режиссера, сценаристов, поэта, композитора, 
  всех исполнителей, даже второстепенных ролей, и не было только одной фамилии 
  - моей. Будь это в одной газете, я бы счел это опечаткой, недосмотром редактора, 
  но в двух, и центральных, - это не могло быть случайностью..." <br>
  <br>
  Дальнейшие события подтвердили правоту Утесова - его не просто забыли, его пытались 
  "замолчать". Тактика замалчивания любимого народом артиста, популярнейшего певца 
  была пущена в ход и позднее, когда по решению правительства проводилось награждение 
  творческой группы фильма. Г. Александрову был вручен орден Красной Звезды, Л. 
  Орлова удостаивалась звания заслуженной артистки. Утесов же получил всего лишь 
  памятный подарок - фотоаппарат. Подобное "распределение" выглядело особенно 
  обидным потому, что приурочивалось к памятному юбилею - пятнадцатилетию советского 
  кино. <br>
  <br>
  К счастью, как все большие, настоящие мастера культуры, Утесов был чужд тщеславия, 
  предпочитая наградам и премиям дружные аплодисменты битком набитого зала, горящие 
  глаза зрителей, их губы, повторяющие вслед за певцом слова песни о ротном запевале: 
  <br>
  <br>
  ...А голоса коль не хватало, <br>
  Он пел ее сердцем своим. <br>
  <br>
  Сообщение Совинформбюро о начале Великой Отечественной войны застало Утесова 
  на репетиции новой программы с мирным названием "Напевая, шутя и играя". Буквально 
  в несколько недель оркестр готовит новую программу - "Бей врага" - и выезжает 
  на фронт. Война, священная народная война порохом и кровью вписывает в репертуар 
  Утесова новые названия: "Мишка-одессит", "Жди меня", "В землянке", "Солдатский 
  вальс", "Заветный камень", "Под звездами балканскими". Окончится война, отгремят 
  последние залпы победного салюта, но еще долгие годы и десятилетия будут звучать 
  нестареющие фронтовые песни Утесова - эхо бушевавшей над нашей родиной и не 
  сломившей ее военной грозы. А пока... Пока оркестр Утесова, как и вся страна, 
  живет в военном фронтовом режиме. Утесовцы выезжают с концертами на оборонные 
  предприятия, сборные пункты, в госпитали, выступают на передовой... <br>
  <br>
  Летопись Великой Отечественной сохранила трогательные свидетельства любви народа 
  к своему певцу, восхищения его талантом. На ленинградском направлении небольшое 
  подразделение наших бойцов с большим трудом удерживало небольшой островок у 
  побережья Балтийского моря. Бои шли ожесточенные, но оборонявшиеся, несмотря 
  на потери, не теряли присутствия духа. Находили даже время и силы шутить по 
  поводу неудачного названия острова - Фриц. И вот в одно из коротких затиший 
  между боями кому-то из бойцов пришло в голову... переименовать остров! Предложение 
  было встречено с энтузиазмом. Так в Балтийском море появился новый остров - 
  Леонида Утесова. <br>
  <br>
  А когда очередная атака фашистов была отбита, в батальонном боевом листке защитники 
  острова, прочитали вот такую злободневную частушку: <br>
  <br>
  Мы назвали островок <br>
  Именем Утесова. <br>
  Лез фашист, а взять не смог - <br>
  Чурка стоеросова. <br>
  <br>
  С первого года войны до победного сорок пятого громила врага в воздухе эскадрилья 
  самолетов "Веселые ребята", построенная на средства утесовского джаз-оркестра. 
  "Со дня получения вашего подарка летчики эскадрильи уничтожили в воздушных боях 
  девять вражеских самолетов, - писал Леониду Осиповичу командир "Веселых ребят" 
  капитан Виталий Попков. - Нет сомнения, что этот счет будет расти". "Веселые 
  ребята" сдержали свое обещание. Когда наши войска подходили к стенам Берлина, 
  на фюзеляже самолета Героя Советского Союза Виталия Ивановича Попкова красовались 
  сорок "звездочек", каждая из которых означала сбитый самолет противника. <br>
  <br>
  Вклад Леонида Осиповича Утесова и его прославленного коллектива в дело победы 
  над врагом был высоко оценен воюющей страной - в самый разгар военных действий 
  он был удостоен звания заслуженного артиста РСФСР. А когда 9 мая Утесов со своим 
  джазом появился на запруженной ликующими москвичами площади Свердлова, на груди 
  его поблескивал орден Трудового Красного Знамени. Эта дорогая для певца награда 
  стала, разумеется, не последней - со временем к ней "присоединились" еще один 
  орден Трудового Красного Знамени, орден Октябрьской Революции, множество медалей 
  и других знаков отличия. Леонид Осипович Утесов самым первым из эстрадных артистов 
  был удостоен звания народного артиста. Время подтвердило справедливость такой 
  высокой оценки - творчество Утесова стало настоящей лабораторией отечественной 
  песни, школой, из которой вышли все наши выдающиеся эстрадные певцы. <br>
  <br>
  "Для меня как артиста песня имела исключительное значение, - размышляет о своем 
  творческом пути народный артист СССР Утесов в книге воспоминаний, - она помогла 
  мне, лично мне, вступать в контакт со зрителем, ощущать себя заодно со всеми, 
  быть в общем единстве. В песне, которая шла непосредственно от сердца к сердцу, 
  возникал мой диалог с людьми. Через песню я мог делиться с ними своими мыслями 
  и чувствами. Мои песни - это моя лирическая речь, обращенная ко всем..." <br>
  <br>
  Иными словами: <br>
  <br>
  Я песне отдал жизнь сполна. <br>
  В ней - жизнь моя, моя работа. <br>
  Ведь песня людям так нужна, <br>
  Как птице крылья для полета! <br>
  <br>
  ...Среди самых больших удач в своей жизни я числю личное знакомство с Леонидом 
  Осиповичем. Почти двадцать лет я имел счастье встречаться с этим замечательным 
  человеком, слушать его, разговаривать с ним... Леонида Осиповича трогала моя 
  увлеченность искусством, в котором он прожил всю свою жизнь, и я в самые разные 
  периоды своей жизни постоянно ощущал его отеческое, доброе отношение к себе. 
  Даже мое имя - Валерий - он переиначил на свой лад, называя попросту Лерой... 
  Меня же, тогда еще молодого человека, пленяли не только мягкость и сердечность 
  Утесова, но и его бесподобный одесский юмор. Вспоминается такой случай - мне 
  предложили принять участие в жюри конкурса солдатской песни. Узнав, что жюри 
  возглавляет Утесов, я сначала ужасно обрадовался, но потом, вспомнив, что во 
  время конкурса будет проходить диспансеризация, на которую я как военнослужащий 
  обязан явиться, здорово приуныл. Однако делать нечего - пошел отпрашиваться 
  у Утесова. <br>
  <br>
  - Леонид Осипович, отпустите меня на диспансеризацию. <br>
  <br>
  Густые брови Утесова "удивленно" взметнулись вверх. <br>
  <br>
  - Диспансеризация?! А с чем это едят, Лерочка? Я, как мог, объяснил. <br>
  <br>
  Утесов загадочно ухмыльнулся. <br>
  <br>
  - Врачи? А зачем они вам, Лера? И без анализов видно, какой вы здоровяк. Прямо 
  кровь с молоком. Это я, знаете ли, кровь с сахаром... <br>
  <br>
  Он с трогательной заботливостью следил за тем, что происходит в моей жизни, 
  звонил, приглашал к себе. В 1976 году у меня родился сын, которого я решил назвать 
  в честь Утесова Леонидом. Узнав об этом, Леонид Осипович позвал меня к себе 
  и вручил свою только вышедшую в свет книжку воспоминаний, предварительно сделав 
  в ней вот такую запись: <br>
