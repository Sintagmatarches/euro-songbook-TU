  <br>
  Гарри Уоррен (Сальваторе Энтони Гуарагна, 1893-1981) — один из популярнейших 
  американских композиторов 20-х — 50-х гг. Он был одним из 11 детей в семье бруклинского 
  сапожника. Мальчик с раннего детства увлекся музыкой, самостоятельно обучился 
  игре на аккордеоне, а в 15 лет уже играл на ударных инструментах в духовом оркестре 
  Джона Виктора на карнавальных шествиях.<br>
  <br>
  Начиная с 1929 г., Гарри Уоррен пишет музыку к кинофильмам: сначала для студии 
  «Парамаунт», затем для «Уорнер Бразерс». В 1940-1945 гг. он работает для киностудии 
  «Твентиз Сенчури Фокс», где и встречается с Маком Гордоном. Из 128 песен, написанных 
  Уорреном для 75 фильмов, двенадцать занимали первые места в хит-парадах.<br>
  <br>
  Мак Гордон (1904-1959) — один из известных американских авторов-лирицистов, 
  создававших песенные тексты для кинофильмов. Родился в Варшаве, семья эмигрировала 
  в США, в Нью-Йорк. Мальчиком Мак пел партию сопрано в менестрельном шоу. Потом 
  начал придумывать и разыгрывать комедийные сценки и на некоторое время стал 
  поющим комиком в варьете. В конце 20-х годов он начинает писать слова к песням, 
  а иногда и музыку для кинофильмов. С 1940 г. работает с Гарри Уорреном. Временами 
  Гордон сам пишет музыку для собственных текстов. Пример тому такие хиты, как 
  «My Heart is an Open Book», «This is the Beginning of the End» и «Here Comes 
  Cookie» (пророческое название, за полвека до Интернета!).<br>
