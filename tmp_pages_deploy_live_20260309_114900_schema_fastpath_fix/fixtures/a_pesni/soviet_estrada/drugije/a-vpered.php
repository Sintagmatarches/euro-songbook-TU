  В том же 1809 году, когда в Тироле вспыхнуло восстание во главе с Андреасом 
  Гофером, Наполеон подписал ряд декретов, положивших начало формированию новых 
  гвардейских полков. Эти полки, получившие вскоре наименование "молодая гвардия", 
  приняли участие в разгроме выступившей согласованно с Гофером австрийской регулярной 
  армии. Поражение австрийцев привело к подавлению восстания и к расстрелу его 
  руководителя. Героическая смерть Андреаса Гофера послужила толчком к созданию 
  песни "Zu Mantua in Banden". В начале XX века на мелодию этой песни немецкими 
  социал-демократами был написан текст "Dem Morgenrot entgegen", в котором рефреном 
  проходят слова "die junge Garde" - "молодая гвардия".<br>
