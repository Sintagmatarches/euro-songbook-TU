  Обе песни, на слова Безыменского и Жарова, стали, в сущности, гимнами комсомола 
  и пионерии. Также с 1922 года существуют издательство "Молодая гвардия" и журнал 
  "Молодая гвардия" - совершенно очевидно, что поэтический образ Айльдерманна 
  ("Wir sind die junge Garde des Proletariats") произвел тогда неизгладимое впечатление 
  на пропагандистов из ЦК комсомола. Впрочем, никто из них никогда в этом не признался.<br>
  <br>
  Подводя итоги: Айльдерманн взял мелодию Кнебельсбергера и написал к ней совершенно 
  иной текст, нежели текст Мозена о тирольском национальном герое. Именно Айльдерманн 
  ввел в оборот термин "молодая гвардия" применительно к левому молодежному движению. 
  Безыменский взял текст Айльдерманна и выполнил его перевод, оставив мелодию 
  неизменной. За рубежом, даже в социалистических странах, об этом знали все и 
  всегда, но в советских песенниках напротив названия "Молодая гвардия" неизменно 
  стояло: "Автор текста Александр Безыменский, музыка народная".<br>
  <br>
  Это примерно то же самое, что называть, например, М. Лозинского автором, а не 
  переводчиком, трагедии "Гамлет, принц Датский".<br>
  <br>
  В 1924 году Владимир Маяковский в известном стихотворении "Юбилейное" дал А. 
  Безыменскому следующую (быть может, излишне резкую) характеристику:<br>
  <br>
  … Надо,<br>
  чтоб поэт<br>
  и в жизни был мастак.<br>
  Мы крепки,<br>
  как спирт в полтавском штофе.<br>
  Ну, а что вот Безыменский?!<br>
  Так…<br>
  ничего…<br>
  морковный кофе.<br>
  <br>
  Александр Ильич Безыменский (кстати говоря, "Безыменский" - это лишь псевдоним, 
  ставший фамилией) всю свою жизнь представлял, пользуясь выражением того же Маяковского, 
  "атакующий класс". Он подписывал, клеймил и громил. "Прежде всего, я член партии, 
  // А стихотворец - потом" - это ведь его слова. Непримиримый враг М.А. Булгакова, 
  он явился одним из прототипов Ивана Бездомного, персонажа "Мастера и Маргариты".<br>
  <br>
  Текст "Молодой гвардии" впоследствии безжалостно и бессовестно переписывался 
  в соответствии с конъюнктурой. К примеру, раскроем сборник "Мы жизни выходим 
  навстречу…", который стотысячным тиражом был выпущен в 1963 году издательством 
  "Молодая гвардия" в рамках библиотечки "Вступающему в комсомол". Этот сборник 
  и открывается песней "Молодая гвардия" (естественно, "Слова А. Безыменского, 
  музыка народная"), но, помимо названия, от приведенного выше первоначального 
  ее текста остались только лишь первая строка ("Вперед, заре навстречу") и рефрен 
  ("Мы, молодая гвардия рабочих и крестьян"). Всё остальное - переписано в соответствии 
  с текущим социальным заказом. Взгляните, как верный "член партии" А.И. Безыменский 
  отделал последнюю строфу:<br>
  <br>
  Советская Отчизна<br>
  Весь мир ведет вперед,<br>
  И солнце Коммунизма<br>
  Над Родиной встает.<br>
  Счастливым сделать шар земной<br>
  Клянемся партии родной,<br>
  Мы, молодая гвардия<br>
  Рабочих и крестьян!<br>
  <br>
  Безусловно, эти и им подобные строки "стихотворец" А.И. Безыменский мог бы с 
  полным правом называть своими. Да только вот кто их сейчас помнит, эти строки… 
  Пропаганда "отменила" их еще при жизни их автора.<br>
