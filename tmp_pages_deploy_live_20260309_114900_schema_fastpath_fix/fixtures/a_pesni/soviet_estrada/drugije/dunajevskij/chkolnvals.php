  <br>
 <span class="header"><strong>И &quot;ШКОЛЬНЫЙ ВАЛЬС&quot; ОПЯТЬ ЗВУЧИТ ДЛЯ НАС<br>
  Как воронежская школьница предложила Исааку Дунаевскому написать песню о своей 
  любимой учительнице</strong><br></span>
  <em><br>
  Газета &quot;Труд&quot;. 12.10.2000.</em><em> №191.</em><br>
  <br>
  Меня всегда поражала необыкновенная заинтересованность Исаака Осиповича Дунаевского 
  в судьбах совершенно незнакомых ему людей, большинство из которых он так в своей 
  жизни и не встретил. Уже после смерти композитора, перебирая в его осиротевшей 
  московской квартире на Можайском шоссе письма, адресованные Исааку Осиповичу, 
  я видел сделанные его рукой пометки &quot;ответить&quot;, &quot;помочь&quot;, 
  &quot;отправить ноты&quot;. Я знаю, каким счастьем и радостью было общение, 
  пусть даже заочное, с этим удивительным человеком. Я переписывался с Дунаевским 
  с 1952 года. Исаак Осипович поддерживал мои первые музыкальные опыты, помог 
  поверить в себя и выбрать дорогу в жизни. Но сегодня я хочу рассказать об истории 
  другой переписки, о которой поведала мне Юлия Сергеевна Меркушева (в девичестве 
  Плахотник), врач-рентгенолог одной из московских поликлиник.<br>
  <br>
  - Весной 1950 года, - рассказывала Юлия Сергеевна, - мы с подругами заканчивали 
  10-й класс и задумали написать к выпускному вечеру песню - вернее, стихи под 
  какую-нибудь известную мелодию. Но какие из нас поэты - сумели лишь переиначить 
  известные слова &quot;На деревне расставание поют&quot; и написали: &quot;В 
  нашей школе расставание поют, провожают наш десятый в институт&quot;... Дальше 
  ничего не сочинялось. А так хотелось, чтобы у выпускников была своя песня! Вот 
  я и решилась написать Дунаевскому.<br>
  <br>
  Сейчас уж и не припомню, что писала. Наверное, наивно, но искренне. О благодарности 
  учителям, о том, как жалко расставаться со школой, с подругами-одноклассницами 
  и вообще с детством и юностью. И особо - о нашей любимой учительнице, директоре 
  школы Антонине Григорьевне Серотинкиной. Мы все очень любили ее, а поэтому и 
  песню просила посвятить именно ей... Заклеила письмо в конверт, написала: &quot;Москва. 
  Композитору Дунаевскому&quot;. И все. Надеялась - и без адреса дойдет. И ведь, 
  правда, дошло.<br>
  <br>
  Вскоре получила ответ, хотя не очень на это рассчитывала.<br>
  <br>
  ...Вот это письмо. Юлия Сергеевна хранит его, как святыню:<br>
  <br>
  &quot;5 апреля 1950 года. Москва.<br>
  <br>
  Милый товарищ Плахотник! Ваше предложение о написании песни для десятиклассниц 
  Воронежской женской школы я получил. Я очень хочу, чтобы вы и ваши подруги по 
  классу поняли, почему я не могу исполнить вашу просьбу.<br>
  <br>
  Писать песню для школы (и не одну) очень нужно. В этой песне надо воспеть любовь 
  к школе, любовь и уважение к педагогам, которые воспитывают в нашей школьной 
  молодежи качества больших советских людей, открывают перед нею широкую и светлую 
  дорогу.<br>
  <br>
  Но почему такую песню надо писать специально для воронежской школы?..<br>
  <br>
  Песня должна воспевать общие, дорогие всем чувства и думы. Хорошая такая песня 
  объединяет миллионы советских людей. И только к таким песням должны стремиться 
  мы, композиторы и поэты. Надо написать такие песни для школы, чтобы они пелись 
  и в Воронеже, и в Москве, и в Рязани, и на Сахалине. И надо обязательно, чтобы 
  в каждой песне была своя Антонина Григорьевна, то есть тот прекрасный человек 
  и педагог, воспоминание о котором уносишь с благодарностью из школы на всю жизнь.<br>
  <br>
  Я думаю, что вы со мной согласитесь и простите мне мой принципиальный отказ 
  исполнить вашу просьбу. Но она подтолкнет меня на работу над песней о выпускниках 
  школы. Кстати, такая песня нам действительно нужна. И, когда я буду работать 
  над ней, я вспомню и вашу просьбу, и вашу любовь к школе и ее директору. И за 
  эту творческую помощь я шлю вам мою заблаговременную благодарность...&quot;.<br>
  <br>
  Композитор не забыл свое обещание, тем более что вскоре его друг и соавтор, 
  поэт Михаил Матусовский принес ему как раз стихи о школе.<br>
  <br>
  Михаил Львович Матусовский вспоминал:<br>
  <br>
  - Сначала я написал стихи &quot;Первая учительница&quot;, а Дунаевский сложил 
  одноименный романс. Смею вас уверить, это было произведение, имеющее полное 
  право на самостоятельную жизнь, но композитора все что-то не устраивало, и однажды 
  он мне сказал: &quot;А может быть, попробовать с другого конца?..&quot; Достал 
  коробку &quot;Казбека&quot;, на которой в уголке острым карандашом (он любил 
  остро отточенные карандаши) были написаны нотные строчки, поставил коробку на 
  пюпитр рояля и заиграл мелодию, которую теперь знают все.<br>
  <br>
  Я воскликнул: &quot;Конечно, именно это!&quot;<br>
  <br>
  А Дунаевский улыбнулся: &quot;Тогда вам придется и в стихах поискать нечто новое... 
  Композитор и поэт должны идти навстречу друг другу, чтобы найти то сочетание 
  стихов и музыки, которое рождает сплав, называемый песней&quot;.<br>
  <br>
  А вот как сам Исаак Осипович рассказывал об истории &quot;Школьного вальса&quot;:<br>
  <br>
  - Поначалу был замысел создать песню об учительнице. Я получал много писем от 
  учащихся с просьбой написать такую песню. Но она мне не удавалась. Получалась 
  либо инфантильная песенка полудетского характера, либо обычная массовая лирика... 
  Тогда я вспомнил, что у нас практикуются школьные балы с приглашением тех, кто 
  раньше окончил эту школу и уже давно вошел в жизнь, - и совершенно перевернул 
  всю прежнюю идею песни и задумал такой школьный вечер, вечер трогательных воспоминаний 
  о школе.<br>
  <br>
  Тема учительницы вошла в этот замысел как неотъемлемая часть воспоминаний. Формой 
  песни стал вальс. Вальс господствует, вальс звучит, и его звуки будят воспоминания. 
  Это - мысли и чувства человека под лирику вальса, которой все подчинено. Кончился 
  вальс, и с ним кончаются как бы все эти милые, дорогие видения прошлого, которые 
  вливаются в настоящее и устремлены в будущее. Это - грустное чувство, потому 
  что грустью, доброй, хорошей грустью, овеяны всегда у человека воспоминания 
  о невозвратно ушедших годах юности.<br>
  <br>
  В двадцатых числах июня я присутствовал на выпускном вечере в художественной 
  школе, которую кончал сын. Когда мне было предоставлено слово, я поднялся на 
  эстраду и исполнил &quot;Школьный вальс&quot;. Успех был огромен. Я подумал, 
  что это, может быть, успех вежливости по отношению к неожиданному концертному 
  номеру &quot;знатного гостя&quot;. Но оказалось иначе...<br>
  <br>
  Песню исполнили по радио, записали на пластинку - и посыпались письма, требования 
  нот. Певцы и певицы стали бегать в поисках нот, переписывать друг у друга. До 
  меня доходят вести об очень большом успехе вальса. Летом я его исполнил в Высшей 
  партийной школе, в Академии общественных наук и, наконец, с большой опаской 
  в пионерской аудитории, в двух лагерях около Рузы. Успех удивительно единодушный.<br>
  <br>
  ...Известно, что Исаак Осипович всегда очень ответственно подходил к первым 
  исполнениям своих произведений. Он сам их инструментировал и делал это блестяще, 
  сам дирижировал оркестром на записи для радио или к кинофильмам, в авторских 
  концертах.<br>
  <br>
  Так было и со &quot;Школьным вальсом&quot;. Право первого исполнения этой песни 
  Дунаевский доверил Георгию Виноградову, выдающемуся певцу и замечательному интерпретатору 
  советской песни.<br>
  <br>
  Вот что вспоминал Георгий Павлович:<br>
  <br>
  - Записывал я эту песню в студии звукозаписи с симфоническим оркестром, которым 
  дирижировал Исаак Осипович Дунаевский. Не успел тогда отзвучать ее финальный 
  аккорд, как распахнулась дверь студии и Дунаевский, взволнованный и растроганный, 
  стремительно бросился мне навстречу, обнял, расцеловал и тут же вручил рукописный 
  клавир, предварительно им надписанный. Это была не первая моя встреча с ним 
  на записях песен. Но &quot;Школьным вальсом&quot;, по-моему, Исаак Осипович 
  особенно дорожил и был очень строг и требователен к его исполнительской трактовке...<br>
  <br>
  Одну из радиопередач, в которой прозвучал &quot;Школьный вальс&quot;, услышала 
  и бывшая воронежская школьница, в то время уже студентка Московского медицинского 
  института Юля Плахотник, написавшая когда-то на конверте &quot;Москва. Композитору 
  Дунаевскому&quot; и вложившая в него доверчиво-наивное, искреннее и трогательное 
  письмо. Ей показалось, что песня эта как-то уж очень перекликается с тем, о 
  чем она писала когда-то Исааку Осиповичу. И она вновь села за письмо композитору.<br>
  <br>
  Ответ пришел буквально через два дня:<br>
  <br>
  &quot;Дорогая Юля! (Я смотрю, Ю. Плахотник, значит, Юлия. Верно?)<br>
  <br>
  Ваше письмо меня тронуло очень глубоко. Оно послужило тем отзвуком на мой &quot;Школьный 
  вальс&quot;, которого я, как это ни покажется странным, ждал. Да, ждал, и именно 
  от вас.<br>
  <br>
  Я не скажу, что мысль о создании школьной песни явилась у меня в связи с вашим 
  тем, давним письмом. Нет! О школьных песнях я всегда думал, да и писал их если 
  не по прямому назначению и если не на прямую тему, то, во всяком случае, на 
  близкую к школьной тематике.<br>
  <br>
  Но ваше письмо, безусловно, ускорило то творческое внутреннее брожение, которое 
  является залогом создания произведения...<br>
  <br>
  Считаю своим долгом послать вам на память экземпляр &quot;Вальса&quot;.<br>
  <br>
  При творческих встречах я всегда рассказываю о &quot;Школьном вальсе&quot; и 
  начинаю так: &quot;Некоторое время тому назад ученицы 10-го класса Воронежской 
  женской средней школы обратились ко мне...&quot; и т.д. Как видите, я никогда 
  не забываю вас и ваших подруг...&quot;<br>
  <br>
  Вскоре пришла бандероль с обещанным клавиром. К тому времени &quot;Школьный 
  вальс&quot; еще не был опубликован - и потому Исаак Осипович прислал рукопись, 
  на лицевом листе которой написал: &quot;Юлии Плахотник с удовольствием дарю 
  на память о письме, &quot;зачавшем&quot; этот вальс. И.Дунаевский. 26.3.1952 
  г.&quot;<br>
  <br>
  Свою музу Исаак Осипович не забывал и позже. Никогда не видя ее, он живо интересовался 
  делами Юлии Сергеевны, посылал ей поздравительные открытки и телеграммы.<br>
  <br>
  И сейчас она раскладывает их на столе, бережно перебирая драгоценные, пожелтевшие 
  листочки...<br>
  <br>
  &quot;Инициатору &quot;Школьного вальса&quot; шлю мое дружеское поздравление...&quot;<br>
  <br>
  &quot;Милая Юля! Что же вы умолкли? Недавно был в Воронеже с концертами. На 
  творческих встречах рассказывал историю &quot;Школьного вальса&quot; и однажды 
  пришлось на вопрос, кто писал мне из Воронежа, назвать вашу фамилию. Среди публики 
  оказались ваши товарищи. Юля, напишите мне как живете, как учитесь...&quot;<br>
  <br>
  ...И вот мы вместе с Юлией Сергеевной слушаем запись &quot;Школьного вальса&quot;. 
  Он опять звучит для нас.<br>
