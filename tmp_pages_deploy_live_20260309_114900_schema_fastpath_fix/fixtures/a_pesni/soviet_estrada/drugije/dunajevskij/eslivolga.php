	<span class="header"><strong>ЕСЛИ 
ВОЛГА РАЗОЛЬЕТСЯ…</strong><br></span> 
Из кинофильма «Вратарь» (1936)<br>
<br>
Музыка Исаака Дунаевского<br>
Слова Василия Лебедева-Кумача<br>
<br>
1. Много горя и страданья сердце терпит невзначай. <br>
Милый скажет: &quot;До свиданья...&quot; Сердце слышит: &quot;...и прощай&quot;. 
<br>
Наперед не угадаешь, где судьбу свою найдешь. <br>
Коль полюбишь, пострадаешь, эту песню запоешь. <br>
<br>
<em>Припев:</em><br>
<br>
Если Волга разольется, <br>
Трудно Волгу переплыть. <br>
Если милый не смеется, <br>
Трудно милого любить. <br>
Без луны на небе мутно, <br>
А при ней мороз сильней. <br>
Без любви на свете трудно, <br>
А любить еще трудней. <br>
<br>
2. Я девчонка молодая. Что мне делать, как мне быть? <br>
Оттого я и страдаю, что не знаю, как любить. <br>
Крепко любишь — избалуешь, мало любишь — отпугнешь, <br>
Беспокойный — ты ревнуешь, а спокойный — нехорош. <br>
<br>
<em>Припев. </em><br>
<br>
3. Счастье — птичка-невеличка, нет ни клетки, ни гнезда, <br>
То погаснет, точно спичка, то зажжется, как звезда. <br>
Нету кушанья без соли, если вышла — так купи. <br>
Нету радости без боли, если любишь — так терпи.<br>
<br>
<em>Припев. </em><br>
<br>
4. Видно, вкус мой изменился. Что поделать мне с собой? <br>
Карий глаз вчера приснился, а сегодня — голубой. <br>
Трудно в маленьких влюбляться, как их будешь обожать: <br>
Целоваться — нагибаться, провожать — в карман сажать? <br>
<br>
Припев. <br>
