	<span class="header"><strong>ЗВАТЬ 
ЛЮБОВЬ НЕ НАДО</strong><br></span> 
Из кинофильма &quot;Моя любовь&quot; (1940)<br>
<br>
Музыка Исаака Дунаевского<br>
Слова Анатолия Д'Актиля<br>
<br>
Звать любовь не надо, явится незванной, <br>
Счастье расплеснет вокруг. <br>
Он придет однажды, ласковый, желанный, <br>
Самый настоящий друг. <br>
Взглядом ты его проводишь,<br>
День весь как во сне проходишь... <br>
Ночью лишь подушке, девичьей подружке, <br>
Выскажешь свои мечты.<br>
<br>
Если всё не так, если всё иначе, <br>
Если ночь полна цветами,<br>
Если сон бежит, если сердце плачет, <br>
Плачет благодарными слезами. <br>
Если целый мир стал для сердца тесен, <br>
Если воздух полон песен. <br>
Если расставаясь, ищешь встречи вновь, <br>
Значит, ты пришла, моя любовь! <br>
<br>
Будут всё нежнее радостные встречи, <br>
Будет каждый день — весна! <br>
Но однажды в темный, непогожий вечер <br>
Ты придешь домой одна! <br>
Медленно платок развяжешь, <br>
Слова никому не скажешь... <br>
Ночью лишь подушке, девичьей подружке, <br>
Выплачешь свою тоску.<br>
<br>
Если всё не так, если всё иначе, <br>
Если думать нету воли, <br>
Если грудь теснит, если сердце плачет, <br>
Плачет от невысказанной боли, <br>
Если дни свои этой болью меришь, <br>
Если никому не веришь, <br>
Если, расставаясь, встреч не хочешь вновь, -<br>
Значит, ты ушла, моя любовь! <br>
