<br>
До свиданья, подруга моя. <br>
На перроне оркестры играют. <br>
Уезжаю с ребятами я. <br>
Звуки марша вдали замирают. <br>
<br>
А на сердце тревожно чуть-чуть. <br>
Внемлет лес паровозному гуду. <br>
Ты меня вдалеке, вдалеке не забудь, <br>
Я тебя никогда не забуду. <br>
<br>
Сторона, где я жил, где я рос, <br>
Где сияло мне школьное зданье, <br>
Где я слышал дыхание гроз, <br>
Где счастливый я шел на свиданья. <br>
<br>
Ты в полку согреваешь мне грудь, <br>
Край, подобный прекрасному чуду.<br>
Ты меня вдалеке, вдалеке не забудь, <br>
Я тебя никогда не забуду. <br>
<br>
Будто вижу себя я в кино, <br>
Там я ладно иду с автоматом. <br>
И когда отслужу - все равно <br>
Я навеки останусь солдатом. <br>
<br>
Ты, мой полк, продолжаешь свой путь <br>
Я душою с тобою повсюду. <br>
Ты меня вдалеке, вдалеке не забудь, <br>
Я тебя никогда не забуду. <br>
