Бубу Касторского сыграл и озвучил Борис Сичкин, а скрипача, ему аккомпанировавшего - Ян Френкель. На экраны фильм вышел в мае 1969 года. В титрах  указано: &quot;Композитор Ян Френкель, тексты песен Р. Рождественского, И. Гофф, Э. Радова&quot;. В фильме три песни, помимо перекочевавшей из первого фильма &quot;Песни дьяволят&quot;: это <a href="ruspole.htm">&quot;Русское поле&quot;</a> на стихи Инны Гофф, &quot;Погоня&quot; на стихи Роберта Рождественского и &quot;Я - одессит&quot;. Соответственно, автор ее текста - известный конферансье Мосэстрады Эмиль Радов (1928-1990). Все свои репертуары, в том числе стихотворные, он писал сам. Многие его стихи стали песнями.<br>
