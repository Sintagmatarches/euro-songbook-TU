 <br>
  <br>
        Польский фокстрот <a href="../popular-in/polska/podsamovarem.php">&quot;Pod 
        samowarem&quot;</a>, получивший большую популярность в СССР в исполнении 
        Леонида Утесова. Написан в 1929 году Фанни Гордон (польск. Fanny Gordon), 
        в будущем - Фаиной Квятковской (польск. Faina Kwiatkowska), проживавшей 
        тогда в Польше уроженкой Ялты, для варшавского кабаре &quot;Морское око&quot; 
        (&quot;Morskie oko&quot;) на слова владельца кабаре Анджея Власта (Andrzej 
        Włast). Русский текст написан Фанни Гордон по предложению фирмы &quot;Polydor 
        Records&quot; для записи на пластинку, которая в 1933 году поступила в 
        продажу в Риге. <br>
  <br>
        В том же году песня заимствована Утесовым и в феврале 1934 года записана 
        им в Москве без указания автора. В те дни в Москве шло создание Фабрики 
        звукозаписи, и &quot;У самовара&quot; стала первой советской пластинкой 
        хорошего качества звучания. <br>
  <br>
        В СССР песня продолжала бытовать без указания авторства, а с 1975 года 
        - с фиктивным авторством &quot;обработка Л. Дидерихса, слова В. Лебедева-Кумача&quot;. 
        В 1979 году по требованию самой Фанни Гордон, жившей уже в СССР, ее авторство было официально признано. Поясняя эту ситуацию, историк советской 
        грамзаписи Глеб Скороходов пишет, что первый экспериментальный экземпляр 
        утесовской пластинки вышел с бумажной этикеткой, подписанной от руки: 
        &quot;У самовара. Леонид Утесов&quot; (Г. Скороходов. Тайны граммофона. 
        М., Эксмо, Алгоритм, 2004, стр. 319-320), а в тираж пластинка вышла с 
        указанием только исполнителя - Утесова и обработчика мелодии - Симона 
        Кагана (Г. Скороходов. Необходимое уточнение / &quot;Аргументы и факты&quot;, 
        1991, №13). При переиздании записей Утесова в 1975 году для обхода цензуры 
        Утесовым была придумана фиктивная подпись &quot;обработка Л. Дидерихса, 
        слова В. Лебедева-Кумача&quot;: ни Дидерихса, ни Лебедева-Кумача давно 
        не было в живых, и претензий к их именам у худсовета быть не могло. <br>
  <br>
  Другой широко известный исполнитель русской версии - Петр Лещенко, записавший 
  ее на пластинку венского филиала фирмы &quot;Columbia&quot; в 1933 году (по 
  другим данным - в 1936-37 гг.), добавив к двум строфам Фанни Гордон еще две:<br>
  <br>
  <em>Ночка снежная,<br>
  А у меня на сердце лето.<br>
  Жёнка нежная,<br>
  Пускай завидуют мне это! <br>
  <br>
  Думы мои одне:<br>
  Побыть скорее с ней наедине.<br>
  Тёмна ночка покроет всё,<br>
  Не выдаст нас и будет точка. </em><br>
  <br>
  У фокстрота есть также литовский вариант текста (лит. «Palangos jūroj»), который 
  не позднее 1931 года исполнял в Каунасе и записал на пластинку один из родоначальников 
  литовской эстрады Даниелюс Дольскис (Даниил Дольский, лит. Danielius Dolskis). С текстами Власта и Гордон он не связан:<br>
  <br>
  <em>Palangos jūroj nuskendo mano meilė<br>
  Ir šaltos bangos jos neatiduos... <br>
  <br>
  (В Паланге в море утонула любовь моя<br>
  И холодные волны не вернут ее...)</em><br>
  <br>
  Подробнее см. Георгий Сухно. <a href="../emigr/a-pollesch.htm">Польские песни 
  в репертуаре Петра Лещенко</a> (Польша, 2009).<br>
