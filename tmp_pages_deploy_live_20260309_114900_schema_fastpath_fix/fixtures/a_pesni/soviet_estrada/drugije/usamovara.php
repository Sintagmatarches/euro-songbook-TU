  <br>
  <strong>Необходимое уточнение</strong><br>
  <br>
  Хочу объяснить, откуда появилась версия об авторстве известного поэта. <br>
  <br>
  В 1975 г., то есть через 30 лет после смерти Лебедева-Кумача, мне довелось быть 
  составителем и редактором двух комплектов утесовских записей.<br>
  <br>
  При подготовке второго комплекта возникло желание включить в его программу когда-то 
  запрещенную песню "У самовара".<br>
  <br>
  - Леонид Осипович, - обратился я к Утесову, рассматривая старую пластинку, которую 
  мы собирались переписать на пленку, - автор обработки мелодии Семен Каган эмигрировал, 
  все его записи Министерство культуры сняло с производства - полетела уже выпущенная 
  пластинка Изабеллы Юрьевой, Каган ей аккомпанировал. С его фамилией "У самовара" 
  сегодня не пройдет.<br>
  <br>
  - Ну что же, напишем "обработка Л. Дидерихса", - сказал Утесов. Леня - покойник, 
  он не обидится.<br>
  <br>
  - А текст? Здесь вообще не сказано, чьи слова!<br>
  <br>
  - Напишите "народные", - предложил Утесов.<br>
  <br>
  - Но кто же в это поверит!<br>
  <br>
  Леонид Утесов задумался.<br>
  <br>
  - В те годы, - сказал он, - моим постоянным автором был Лебедев-Кумач. Смотрите, 
  почти все, что я записал в 1934 году, его песни - "Подруженька", "У окошка", 
  "Качели", "Папочка и мышки". Мы дружили, и Василий Иванович делал для меня все! 
  Это еще до "Веселых ребят"! Напишем, что он сделал и "У самовара" - к его фамилии 
  наш худсовет не придерется!<br>
  <br>
  Так и появилась в 1975 г. фамилия поэта рядом с песней "У самовара". Знал ли 
  Леонид Осипович подлинного автора музыки и текста этой песни? Знал, познакомился 
  с Фаиной Марковной в 50-е годы, но ее авторство всерьез не принял.<br>
  <br>
  Авторство Ф. М. Квятковской было восстановлено только через несколько лет, но 
  Лебедев-Кумач здесь ни при чем.<br>
  <br>
  <em>Г. Скороходов</em><br>
  <br>
  <br>
  <strong>Судьба одного четверостишия</strong><br>
  <br>
  Излишне напоминать, чем запомнился нам 1937 г. Но, оказывается, поэты в те праздничные 
  дни были настроены бодро, весело - дальше некуда.<br>
  <br>
  Лейб-песенник Вас. Лебедев-Кумач бодро начинает песню "Москва майская":<br>
  <br>
  Утро красит нежным цветом<br>
  Стены древнего Кремля,<br>
  Просыпается с рассветом<br>
  Вся советская земля.<br>
  <br>
  Далее последовательно описывается майская Москва в разное время суток. Вот и 
  сумерки настали:<br>
  <br>
  Вечер тает в белой дымке,<br>
  В ярком пламени зари,<br>
  И на ножке-невидимке<br>
  Блещут бусы-фонари.<br>
  <br>
  Я достал сохранившийся у меня январский номер дореволюционного ежемесячника 
  "Свободный журнал" за 1915 г. На странице 69-й мое стихотворение "Вечер". Всего 
  две строфы. И вот первая:<br>
  <br>
  Город замер в сонной дымке,<br>
  Гаснет зарево зари,<br>
  И на ножке-невидимке<br>
  Блещут бусы-фонари.<br>
  <br>
  Как расстроила меня тогда нелепая опечатка! Ножка - вместо нитки. Я хотел сравнить 
  сияющие в темноте гирлянды фонарей с нанизанными на нитку-невидимку яркими бусами. 
  <br>
  <br>
  Я написал в правление Союза писателей: вот, мол, как один член союза (знаменитый) 
  похищает строки у другого члена союза - рядового. Вскоре получаю телеграмму 
  от поэта Семена Кирсанова - просит сообщить подробности.<br>
  <br>
  Просьбу эту я выполнил. Прошло еще какое-то время. Встречаю в трамвае известного 
  еврейского поэта Переца Маркиша (впоследствии уничтоженного Сталиным). Он рассказал 
  мне, что накануне присутствовал на пленуме правления Союза писателей, где Фадеев 
  осуждал Лебедева-Кумача за вольное обращение с чужими текстами и привел пример 
  с "ножкой" как один из доказательных. Оказалось, что Кирсанов по просьбе Фадеева 
  подобрал примеры, которых обнаружил немало.<br>
  <br>
  Вскоре в "Литературной газете" появился отчет о том пленуме. Но, к удивлению, 
  в нем ничего не было сказано о выступлении Фадеева по адресу Лебедева-Кумача. 
  И я предположил, что некто "авторитетный" посоветовал ему не посягать на честь 
  и достоинство поэта, заявившего еще в 1935 г.:<br>
  <br>
  Я другой такой страны не знаю,<br>
  Где так вольно дышит человек.<br>
  <br>
  Но сам Лебедев-Кумач, очевидно, сделал вывод из этой истории. Вот его "Избранное" 
  (М., Гослитиздат, 1950). Здесь на странице 217 находим ту самую "Москву майскую". 
  Но в ней нет уже ни тающего вечера, ни белой дымки, ни фонарей, ни бус, ни нелепой 
  "ножки". Четверостишие заменено целиком:<br>
  <br>
  День уходит, и прохлада<br>
  Освежает и бодрит,<br>
  Отдохнувши от парада,<br>
  Город праздничный гудит.<br>
  <br>
  <em>А. Палей, член Союза писателей</em><br>
  <br>
  <a name="#2"></a><br>
        <strong>Кто написал "Священную войну"…</strong><br>
  <br>
  Лично мне известны факты, изложенные в статье "Украденная песня", и хочу рассказать 
  о присвоении Лебедевым-Кумачом и Б. Александровым авторства песни "Священная 
  война". Воровства у Лебедева-Кумача было достаточно, и пора бы назвать истинного 
  автора этой песни.<br>
  <br>
  В начале беспокойного XX века в г. Рыбинске жил учитель русского языка Александр 
  Адольфович Баде. Шла первая мировая война, в которой участвовала дореволюционная 
  Россия. Под впечатлением этой войны и родились слова этой песни, музыку к которой 
  тоже написал А. Баде. Было это в 1916 г., а в 1938 г. А. Баде отослал слова 
  и музыку В. Лебедеву-Кумачу, чтобы получить отзыв, а может, и опубликовать песню.<br>
  <br>
  А через три года наша страна услыхала первое исполнение этой песни! Заменив 
  несколько слов, Лебедев-Кумач на второй день войны в июне 1941 г. объявил, что 
  за одну ночь они с Б. Александровым написали песню "Священная война".<br>
  <br>
  Впоследствии дочь А. Баде пыталась восстановить истину, но и ее отец - автор 
  песни, и родные стали куда-то исчезать… Таковы факты.<br>
  <br>
  Мне очень хочется, чтобы узнали правду об истории написания песни "Священная 
  война".<br>
  <br>
  <em>Н. Деревянко, музыкант</em><br>
  <br>
  <em>&lt;В статье в последнем разделе опечатка: "Б. Александров" вместо "А. Александров". 
  Изложенная в статье версия о существовании песни Боде не имеет документального подтверждения - см., например, С. Макин <a href="../ww2/oficial/a-svvojna.php">&quot;Священная 
  война: момент истины&quot;</a>. Прим. a-pesni&gt;</em><br>
  <br>
  <br>
  <br>
  <strong>Из книги Бориса Савченко &quot;Кумиры забытой эстрады&quot;. М.: Знание, 
  1992, стр. 91-94: </strong><br>
  <br>
  Изучая как-то дискографию Леонида Осиповича Утесова, я заметил, что на пластинках 
  разных лет указаны различные авторы имевшей некогда фантастическую популярность 
  песенки «У самовара». Она была известна тогда всей Европе. Ее исполняли на эстраде 
  Петр Лещенко и Константин Сокольский. Включали в свою программу и другие артисты, 
  например созданный из русских эмигрантов хор донских казаков. И сегодня известный 
  видеоклип «Лицедеев» периодически напоминает нам об этом шлягере. На пластинках 
  30-х годов автором значилась некая Фанни Гордон, в 70-е годы — Квятковская, 
  а на втором диске альбома «Памяти Утесова» в создатели песни попали Л. Дидерихс 
  и В. Лебедев-Кумач. Будучи в Ленинграде в гостях у Григория Марковича Полячека, 
  человека поистине энциклопедических знаний по эстрадному искусству, я попросил 
  его помочь мне разобраться, в чем тут дело. Поняв все буквально с полуслова, 
  он тут же полистал свою записную книжку и выдал мне нужный телефон. Так я попал 
  к Фаине Марковне Квятковской — композитору, драматургу, переводчику... Я сидел 
  у нее дома и, пока хозяйка разбиралась с архивом, рассматривал пожелтевший картон 
  с нотами. На лицевой стороне напечатано: «Слова Анджея Власта, музыка Фанни 
  Гордон. Под самоваром. Популярная песня из ревю «Путешествие на луну» в театре 
  «Морске око». 1931». И во весь формат рисунок лихо отплясывающей на фоне огромного 
  самовара парочки. <br>
  <br>
  — Фаина Марковна, — догадался я, — так, значит, Гордон — это... <br>
  <br>
  — Моя девичья фамилия. Эту песенку я написала, когда мне не исполнилось и двадцати 
  лет. Я была, как говорится, музыкальной девушкой. Играла на фортепиано, очень 
  любила слушать русскую классику, особенно Римского-Корсакова. А музыку придумывала, 
  можно сказать, со дня рождения. Как-то раз - мы тогда уже жили в Варшаве - в 
  одном доме родители заставили меня играть мои «произведения». Среди гостей находился 
  директор театра-варьете «Морске око» Анджей Власт. Одна из мелодий ему понравилась. 
  Он попросил сыграть ее еще раз и взял мелодию себе. <br>
  <br>
  Власт придумал оригинальный номер: под гигантским самоваром сидели парень и 
  девушка, они пили чай, а потом начинали танцевать и петь: «Под самоваром щеди 
  моя Маша, я муве «так», а она муве «нет»... Текст песни написал сам Власт - 
  он, кстати говоря, был автором слов всех песен, которые шли у него в театре. 
  Но его стихи показались мне несколько пошловатыми, и я потом переделала их. 
  Правда, в исполнении Леонида Утесова выпал первый куплет, потому что мелодию 
  и текст он взял с немецкой пластинки фирмы «Полидор», а там певец Нюма исполнил 
  «Под самоваром» без начала. <br>
  <br>
  Успех песенки был совершенно невероятный. В этом, кстати, немалая заслуга и 
  пары, танцевавшей на сцене, - балетмейстера Феликса Парнеля и его партнерши 
  Зи-Зи Халяма. Помню, через некоторое время Парнель пришел ко мне, рассказал 
  один сюжет и попросил написать танго «Аргентина». Я выполнила заказ, и танго 
  - оно начиналось так: «Ночью, ночью в знойной Аргентине...» - в их исполнении 
  тоже стало чрезвычайно популярным. <br>
  <br>
  - Оно ведь было модным, говорят, в 50-е годы, только уже в исполнении братьев 
  Лепянских... Значит, судя по рецензиям, дела у вас шли неплохо? <br>
  <br>
  - Да, хотя я была молода, за мной утвердилось реноме популярного композитора. 
  Я писала эстрадные балеты. В Варшаве в театре «8-30» ежевечерне в течение года 
  шла моя оперетта «Нью-Йорк беби, или Яхта любви», где было более двадцати танцевальных 
  и песенных номеров. В 1935 году эта оперетта ставилась в Брюсселе, Антверпене 
  и других городах Европы. <br>
  <br>
  Потом началась война, и у Фаины Квятковской все рухнуло. Был плен, лишения, 
  гетто... Чудом удалось избежать смерти. Когда Варшава была освобождена, всем, 
  кто родился в России, предложили вернуться на родину. Фаина Марковна поехала 
  не раздумывая. <br>
  <br>
  9 Мая 1946 года Квятковская уже выступала в качестве ведущей в Мурманске, в 
  интерклубе, перед нашими и иностранными моряками. Программу вела на английском 
  языке. Постепенно из таких выступлений сложилось целое представление, оно называлось 
  «Кругосветное путешествие». В Калининской областной филармонии она выступала 
  со своим джазом. Конферансье объявлял: «Сейчас наш руководитель Фаина Квятковская 
  даст вам урок английского языка в ритме джаза», и в течение пяти-шести минут 
  джаз-леди вела своеобразный урок грамматики. <br>
  <br>
  С 1947 года Квятковская работает в Ленгосэстраде, пишет детские пьесы, музыкальные 
  фельетоны, песни, переводит польские юморески. В начале 50-х годов на сценах 
  музыкальных театров Ленинграда, Иркутска, Хабаровска состоялась премьера ее 
  оперетты «Девушка из Шанхая»... <br>
  <br>
  - Фаина Марковна, как же все-таки получилась эта маленькая, скажем так, неразбериха 
  с авторством вашей песни? <br>
  <br>
  - Я знаете, человек непритязательный. Видите, у меня даже пианино нет. Хотя 
  в свое время могла бы, наверное, на одном «Самоваре» заработать миллион. Но 
  у меня тогда и в мыслях не было, что есть какие-то формальные вещи. Поют «У 
  самовара» - ну и хорошо. А на фирме «Мелодия», видимо, не очень интересовались, 
  кто истинный создатель того или иного произведения, если оно написано, допустим, 
  еще в довоенное время. Так бы и продолжалось, если бы один мой знакомый не надоумил 
  обратиться во Всесоюзное агентство авторских прав. И справедливость сразу была 
  восстановлена. Могу еще добавить вот что: некоторые композиторы, сочиняя музыку 
  к фильмам, действие в которых происходит в 30-40-е годы, иногда используют мои 
  мелодии. Но хоть бы кто из них позвонил и спросил моего согласия или хотя бы 
  просто поблагодарил... А когда им указывают на живого автора, они попросту разводят 
  руками: «Ну извините - не знали». <br>
  <br>
  Фаина Марковна сварила мне кофе. Я шутливо заметил, что нашей встрече более 
  приличествовал бы душистый чай из самовара. Она так же шутливо парировала мои 
  претензии: «Самовар есть, но с хорошим чаем в городе напряженка, а плохим поить 
  гостей не хочется». <br>
  <br>
  Странная все-таки вещь - популярность. Какова ее истинная значимость, если радио 
  и телевидение чуть ли не каждый день представляют нам все новых и новых композиторов 
  - через месяц-другой мы уже не помним их произведений, - а тут живет и здравствует 
  автор знаменитейшего шлягера «У самовара» - и ничего, даже никто его не знает. 
  Нет, странная вещь - популярность... * <br>
  <br>
  <em>* Осенью 1991 года Ф. Квятковская скончалась.</em><br>
