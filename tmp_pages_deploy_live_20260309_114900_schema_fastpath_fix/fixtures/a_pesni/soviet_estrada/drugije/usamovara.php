  <br>
  В довоенной Польше композитора и поэта Фаину Квятковскую хорошо знали, о чем 
  свидетельствуют чудом сохранившиеся после гитлеровского гетто и советской ссылки 
  газетные вырезки, афишки, программки, фотографии. Все это хозяйство давно пожелтело, 
  рассыпается в руках, но для ленинградки Фаины Марковны - это единственное богатство.<br>
  <br>
  Ее псевдоним был таким: Фанни Гордон. В 1931 г. она написала две песни, которые 
  и до сих пор известны во всем мире: это танго "Аргентина" и фокстрот "У самовара". 

  Нас интересует вторая.<br>
  <br>
  Помните: "У самовара я и моя Маша…"? Песня была написана для варшавского кабаре 
  "Морское око". Собственно, сначала Фанни написала только музыку, а автором текста 
  (польского, естественно) был Анджей Власт, владелец этого кабаре.<br>
  <br>
  В один прекрасный день к Фанни пришли два вежливых немца - представители фирмы 
  "Полидор". Они заключили с ней контракт на выпуск пластинки с "Машей" и "Аргентиной", 
  но, по условиям контракта, песенки должны были исполняться на русском языке, 
  так как пластинку предполагалось распространять в Риге, где русский язык имел 
  большее хождение, чем польский.<br>
  <br>
  А надо сказать, что родилась Фанни в Ялте и полькой была лишь по отчиму. Так 
  что с русским текстом она справилась быстро. Короче, пластинка была записана, 
  и в 1933 уже продавалась в Риге. "Пластинка эта сохранилась, и на ней обозначен 
  автор музыки и слов - "Ф. Гордон". А вскоре из сталинской России в Польшу Пилсудского 
  поступил первый сигнал: с песней "У самовара я и моя Маша…" происходят странные 
  вещи. Фаина Марковна показала мне вырезку из газеты "Курьер Варшавски". Корреспондент 
  С. Вагман сообщал в статье "За красным кордоном":<br>
  <br>
  "Самый большой шлягер в летнем театре в парке - некий фокстрот, который уже 
  несколько месяцев является "гвоздем" всех театральных площадок, кафе, ресторанов, 
  клубов и т. д. Фокстрот этот… польская песенка Власта "Под самоваром" в русском 
  переводе под названием "Маша".<br>
  <br>
  Если бы существовала литературная и музыкальная конвенция между Польшей и Советским 
  Союзом, пожалуй, самыми богатыми на сегодняшний день людьми в Польше были бы 
  Власт и Фанни Гордон".<br>
  <br>
  А случилось вот что: в феврале 1934 г. джаз-оркестр Леонида Утесова записал 
  на пластинку (по образцу, привезенному из Риги) песню "У самовара". Пластинка 
  эта, представьте себе, тоже сохранилась у коллекционеров. И знаете, что указано 
  в выходных данных этого бестселлера? "Обработка Л. Дидерихса - слова В. Лебедева-Кумача"! 
  Утесов оказался умнее - он ограничился исполнительским гонораром. А вот Кумачу 
  попали не принадлежащие ему деньги за написанный не им текст.<br>
  <br>
  "Фаина Марковна, - спросил я у нее, - что же вы так долго молчали, что же не 
  делали попыток восстановить свои права на эту песню? Она ведь до сих пор исполняется, 
  до сих пор выходит на пластинках". "Я боялась", - ответила мне старушка.<br>
  <br>
  В 1945 г. они с матерью переехали в Советский Союз, как они считали, на родину. 
  Родина встретила их неприветливо: пришлось скитаться из города в город, едва 
  хватало на пропитание. Джаз-ансамбль Калининской областной филармонии, которым 
  она руководила, был разогнан, музыканты были репрессированы. Борьба с орденоносным 
  Лебедевым-Кумачом была бы самоубийством. И все же, когда великий песенник советской 
  эпохи, умер, Фаина Марковна предстала перед очами Утесова. Тот долго ахал, обещал 
  разобраться, восстановить справедливость. Увы, она была восстановлена только 
  в 1979 г., когда из фирмы "Мелодия" пришло письмо:<br>
  <br>
  "В связи с письмом ВААП о защите имущественного права и авторского права на 
  имя т. Квятковской Ф. М. управлением фирмы "Мелодия" дано указание Всесоюзной 
  студии грамзаписи начислить причитающийся т. Квятковской Ф. М. гонорар за песню 
  "У самовара", а также исправить допущенную в выходных данных песни ошибку… Генеральный 
  директор П. И. Шабанов.<br>
  <br>
  "Причитающийся" Ф. М. Квятковской гонорар составил 9 рублей…<br>
  <br>
  "Московский комсомолец", "Советская культура", журнал "Советская эстрада и цирк" 
  сообщили: найден автор известной песни! А вот фамилия известного, но самозваного 
  автора - Кумача - даже не упомянули, как будто не красовалась она на пластинках, 
  как будто не заполнялись гонорарные ведомости на концертах и в ресторанах, как 
  будто не капали ему, а затем его семье деньги за каждое исполнение этой песни.<br>
  <br>
  Журнал "Вопросы литературы" в 1982 г. опубликовал фрагменты записных книжек 
  Лебедева-Кумача. Иногда его, оказывается, мучила совесть.<br>
  <br>
  1946 г. датирована запись: "Болен от бездарности и от серости жизни, перестал 
  видеть основную задачу, все мелко, все потускнело. Ну еще 12 костюмов, 3 машины, 
  10 сервизов… И глупо, и пошло, и недостойно… И неинтересно".<br>
  <br>
  Увы, "У самовара" - не единственная песня, которую присвоил себе "самый советский 
  из поэтов" Лебедев-Кумач.<br>
  <br>
        <em>Полностью статья Андрея Мальгина "Самый советский из поэтов" будет 
        опубликована в №? еженедельника "Столица". Подписка на него принимается 
