        <br>
        1. Бублички </strong><br>
        <br>
        Песня «Бублички» родилась во времена нэпа и приобрела необычайную популярность. 
        Особенно в босяцкой и уголовной среде. Песня эта — авторская, но вот по 
        поводу автора существуют расхождения. Дмитрий Сергеевич Лихачёв вспоминал: 
        «Член-корреспондент АН СССР известный литературовед Леонид Тимофеев в 
        молодости сочинил чрезвычайно популярную в подонках общества у шпаны песенку: 
        «Купите бублички»... Под музыку «Бубличков» в 20-е годы танцевали фокстрот, 
        а в Соловецком театре отбивала чечётку парочка — Савченко и Энгельфелдт. 
        Урки ревели, выли от восторга (тем более что Савченко и Энгельфелдт были 
        «свои»). Того же мнения об авторстве «Бубличков» придерживается Ирина 
        Одоевцева, о чем она пишет в книге «На берегах Невы». Но существует и 
        другая версия, приписывающая авторство известному одесскому поэту Якову 
        Ядову. <br>
        <br>
        В фондах музея эстрады, созданного еще в советском Ленинграде Г. М. Полячеком, 
        сохранилась уникальная запись 1956 года. Об истории создания «Бубличков» 
        вспоминает популярный в свое время куплетист Григорий Красавин: «Приехав 
        на гастроли в Одессу, я был поражен, что, пока я ехал с вокзала к Ядову, 
        всю дорогу меня сопровождали возгласы «Купите бублики!» Мне захотелось 
        иметь песенку с таким припевом. О своем желании я сказал Ядову и сыграл 
        на скрипке, с которой обычно выступал, запавшую в память мелодию. Яков 
        Петрович разразился привычным для него бурным смехом и сказал жене Ольге 
        Петровне своим сиплым голосом: «Ставь самовар для артиста, а я буду печь 
        бублики». Полчаса стучала в соседней комнате машинка. В тот же вечер я 
        с листа исполнял «Бублики» в «Гамбринусе». На следующий день вся Одесса 
        пела «Бублики». Об авторстве Ядова писал и К. С. Паустовский. Правда, 
        он ошибся в дате, отнеся создание песни к 1921 году. Причём мелодия, возможно, 
        действительно иноземного происхождения, и именно поэтому под этот фокстрот 
        (по утверждению Ирины Одоевцевой) танцевал весь цивилизованный мир. Эту 
        же историю пересказывает Борис Савченко в своей книге «Кумиры отечественной 
        эстрады». <br>
        <br>
        Позже появились варианты «Бубличков» на идиш, где первая строка исполнялась 
        по-русски, а далее — на еврейском языке: «Купите бублички, койфт майне 
        бейгелах!» (см. также предисловие к песне <a href="papirosy.php">«Купите 
        папиросы»</a>). <br>
        <br>
        Позднейшие добавления к тексту «Бубличков» мною отделены от оригинального 
        текста. <br>
