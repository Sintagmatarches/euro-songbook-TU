  <strong>2. Лягушка</strong><br>
  <br>
  Шел мальчишка вдоль опушки, <br>
  Сам не зная куда. <br>
  По пути поймал лягушку <br>
  С чистого пруда.<br>
  <br>
  Лягушка вдруг говорит ему: <br>
  «Отпусти на волю, милый друг, <br>
  Требуй, что тебе надо, <br>
  Я исполнить буду рада. <br>
  Пусть исполнятся в награду <br>
  Три желания твои». <br>
  <br>
  Но не смог понять мальчишка<br>
  Трех желаний своих толк. <br>
  Заказал он для начала золота мешок. <br>
  Он стал богат. Деньгам он не рад, <br>
  Деньги жизнь его превратили в ад. <br>
  <br>
  «Я хочу добиться власти в том крае родном». <br>
  Ночь прошла, и стал мальчишка <br>
  Наш в том крае королем. <br>
  Он стал богат. Деньгам он не рад. <br>
  Власть и деньги его жизнь превратили в ад. <br>
  <br>
  «Милая моя лягушка, <br>
  Ты меня прости, <br>
  Не хочу я власти, а хочу любви». <br>
  Лягушка вдруг изменила лик — <br>
  И пред ним девушка стоит. <br>
