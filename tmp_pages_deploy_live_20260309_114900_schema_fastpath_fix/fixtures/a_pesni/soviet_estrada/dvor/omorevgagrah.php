        <h1>О, МОРЕ В ГАГРАХ</h1>

Мерный стук колес, и поезд мчится,<br>
Покидая город ваш родной.<br>
Небо ночью звездами искрится,<br>
Утопая в дымке голубой.<br>
<div class="refren">Под покровом теплой южной ночи<br>
Поезд мчит и плещется волна.<br>
Вот уж позади остались Сочи,<br>
    Вас встречают пальмы и луна.</div>
<br>
<em>Припев:</em>
О, море в Гаграх!<br>
О, пальмы в Гаграх!<br>
Кто побывал, тот не забудет никогда!<br>
Мой взор ласкает<br>
И восхищает,<br>
И на горах весною-летом снег всегда.<br>
<div class="refren">Здесь море рокот<br>
    И пальмы шепот,<br>
    Когда шумит и льется к берегу прибой.<br>
    Здесь отдыхая,<br>
    Сил набирая,<br>
    О, город Гагры, восхищаюсь я тобой.</div>
<br>
Здесь у моря царствует природа,<br>
Целый год цветет тенистый сад.<br>
Здесь всегда в любое время года<br>
В воздухе магнолий аромат.<br>
<div class="refren">Кипарисы, пальмы, чуть качая[сь]<br>
Стройною шеренгою встают.<br>
    Солнечному свету улыбаясь,<br>
    Средь магнолий золото дают.</div>
<br>
<em>Припев:</em>
О, море в Гаграх!<br>
О, пальмы в Гаграх!<br>
Кто побывал, тот не забудет никогда!<br>
Мой взор ласкает<br>
И восхищает,<br>
И на горах весною-летом снег всегда.<br>
<div class="refren">Здесь море рокот<br>
    И пальмы шепот<br>
    Когда шумит и льется к берегу прибой.<br>
    Здесь отдыхая,<br>
    Сил набирая<br>
    И уезжая, восхищаюсь я тобой.</div>
<br>
