	<span class="header"><strong>СЕРЕГА-ПРОЛЕТАРИЙ</strong> </span>
  <br>
  <br>
  Служил на заводе Серега-пролетарий, <br>
  Он с детства был испытанный марксист, <br>
  Он был член месткома, и был он член парткома, <br>
  А, в общем, стопроцентный активист.<br>
  <br>
  Евойная Манька страдала уклоном, <br>
  И слабый промеж ними был контакт: <br>
  Накрашенные губки, колени ниже юбки, <br>
  А это, несомненно, вредный факт.<br>
  <br>
  Сказал ей Серега: - Бросай ты эти штучки, <br>
  Ведь ты компроментируешь меня. <br>
  Ты вредная гада, с тобой бороться надо, <br>
  Даю на исправление три дня.<br>
  <br>
  Она ему басом: — Катись ты к своим массам, <br>
  Не буду я в твоей Кэ Пэ Сэ Сэ!<br>
  А он не сдается, он будет с ней бороться,<br>
  Серега на своем лихом посте.<br>
  <br>
  Три дня как один пролетели,<br>
  И сказал ей Серега вот так:<br>
  - Напрасно вы в самом-то деле<br>
  Рассчитываете на брак.<br>
  <br>
  Маруська тогда понимает,<br>
  Что жизнь ее стала хужей,<br>
  И в сердце с размаху вонзает<br>
  Шестнадцать столовых ножей.<br>
  <br>
  Мотор все пропеллеры крутит,<br>
  Москве показаться пора,<br>
  Маруське лежать в институте<br>
  Профессора Пастера.<br>
  <br>
  Маруську на стол помещают<br>
  Шестнадцать дежурных врачей,<br>
  И каждый из них вынимает<br>
  Свой ножик из ейных грудей.<br>
  <br>
  - Вынай, не вынай, не поможет,<br>
  Не быть мне с любимым вместя.<br>
  Оставьте один только ножик,<br>
  На память о милом в грудях.<br>
  <br>
  Маруську везут в крематорий<br>
  И в печь ее прямо кладут.<br>
  В тоске и отчаянном горе<br>
  Серега ее тут как тут.<br>
  <br>
  - Маруся, когда б ты, родная,<br>
  Открыть свои глазки могла.<br>
  Маруся ему отвечает:<br>
  - Нельзя, я уже померла.<br>
  <br>
  Я жизнь всю ее перепортил, <br>
  За это отвечу я сам.<br>
  Насыпьте, пожалуйста, в портфель,<br>
  Мне пеплу четыреста пятьдесят шесть грамм.<br>
  Насыпьте ж, насыпьте ж мне в портфель,<br>
  Мне пеплу четыреста пятьдесят шесть грамм.<br>
