  1. Служил на заводе Серенька-пролетарий...</strong><br>
  <br>
  Служил на заводе Серенька-пролетарий, <br>
  Он в доску был сознательный марксист, <br>
  Он был член профкома, он был член завкома, <br>
  А в общем стопроцентный активист.<br>
  <br>
  Евойная Манька страдала все уклоном, <br>
  Плохой промежду ними был контакт: <br>
  Накрашены губки, колени ниже юбки, <br>
  А это безусловно вредный факт.<br>
  <br>
  И вот рассердился Серенька-пролетарий: <br>
  — Такое не годится никуда! <br>
  Ты, Маня, хуже гада, бороться с тобой надо, <br>
  И с Маньковщиной кончить навсегда.<br>
  <br>
  Марусенька, Машка, ты брось свои замашки,<br>
  Ведь ты компрометируешь мине. <br>
  Она ему басом: — Вались ты к своим массам. <br>
  Не буду я сидеть в твоем клубе! <br>
  <br>
  <em>В нашу гавань заходили корабли. Пермь, &quot;Книга&quot;, 1996.</em><br>
 <br>
  <br>
  <strong>2. Служил на заводе Семен-пролетарий...</strong><br>
  <br>
  Служил на заводе Семен-пролетарий, <br>
  Он в доску был сознательный марксист, <br>
  И он же член профкома, и он же член месткома - <br>
  Ну, в общем, стопроцентный активист. <br>
  <br>
  Жена его Маруся страдала уклоном, <br>
  И наш Семен утратил с ней контакт. <br>
  Накрашенные губки, колени ниже юбки - <br>
  Ну, словом, безусловно мерзкий факт. <br>
  <br>
  Сказал Сенька Маньке: "Ты брось свои отрыжки, <br>
  Ведь ты комплиментируешь себе... <br>
  Маруся ему басом: "Пошел ты к своим массам! <br>
  Не буду я сидеть в твоей клубе!" <br>
  <br>
  Стихийно возмутился тут Сенька-пролетарий, <br>
  И пошла между ними тут мура: <br>
  "Ты - шахтинская гада, и нам таких не надо! <br>
  С помадовщиной кончить нам пора!" <br>
  <br>
  Услышав это дело, тут Манька заревела <br>
  И волосы повсюду с себя рвет! <br>
  А Сенька не сдается, он своего добьется - <br>
  Он маньковщину с корнем изживет! <br>
