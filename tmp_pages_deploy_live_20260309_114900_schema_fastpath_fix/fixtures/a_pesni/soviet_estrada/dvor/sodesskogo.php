  <strong>ВАРИАНТЫ (9) <br>
  <br>
  1. С одесского кичмана</strong> <br>
  <br>
  <em>Из книги Фимы Жиганца &quot;Русский шансон: тексты, ноты, история&quot; 
  Ростов н/Д, Феникс, 2005 (ее первое издание вышло там же в 2001 под загл. &quot;Блатная 
  лирика&quot;) </em><br>
  <br>
  Одна из известнейших так называемых «одесских» песен. Как и многие произведения 
  этого цикла, на самом деле является переделкой песни, имеющей к Одессе отдалённое 
  отношение. Широкой публике стала известна благодаря замечательному исполнению 
  Леонида Осиповича Утёсова. <br>
  <br>
  Ранний вариант этой каторжанской песни даже в исполнению Утёсова начинался несколько 
  иначе. Иван Солоневич, например, в своих мемуарах «Россия в концлагере» (1936) 
  вспоминает: «...Утёсов пел свои блатные песенки: <br>
  <br>
  <em>С вапн<strong>я</strong>рского кичмана <br>
  Сорвались два уркана, <br>
  Сорвались два уркана на Одест...» </em><br>
  <br>
  То есть в оригинале урканы бежали не из одесской тюрьмы, а, напротив, — в Одессу 
  («Одест», «Одеста» — типично каторжанское произношение Одессы). Продолжения 
  всей песни я не раздобыл, но окончание этого куплета мне удалось узнать от одного 
  старого ростовчанина, Бориса Петровича, с которым мы разговорились в ростовском 
  парке Горького, наблюдая за игрой тамошних шашистов. Он вспомнил, что дальше 
  пелось: <br>
  <br>
  <em>«В Одесте на малине <br>
  Они остановились, <br>
  Они остановились, наконец» </em><br>
  <br>
  Откуда бежали «сидельцы», непонятно. Причём написание «вапнярский» представляется 
  не совсем точным. Позже одессит Утёсов, сделав кичман одесским, сохранил «кичманский» 
  эпитет, но прилепил его к уголовной «малине». На пластинке явно слышится «в 
  вапняновской малине». Возможно, это искажённое слово «вапняный», «вапный»; «вапа» 
  на церковнославянском значит известь, таким образом речь идёт о свежевыбеленной, 
  то есть новой тюрьме. Но это лишь версия. А уж позднейшие исполнители для «прояснения» 
  дали кучу своих трактовок утёсовского текста. <br>
  <br>
  «Одесский кичман» пользовался бешеным успехом публики. Практически на каждом 
  концерте от певца требовали исполнения полюбившегося номера. Однако в 30-е годы 
  Комитет по делам культуры запретил Утёсову исполнять «Кичман» со сцены, Запрет 
  был снят только после того, как Леонид Осипович выступил в 1936 году на правительственном 
  концерте в Грановитой палате по случаю беспосадочного перелёта Валерия Чкалова 
  до острова Удд. Певца попросил исполнить его «коронку» в Кремле, по одной версии, 
  герой торжества Валерий Чкалов, по другой — сам Сталин через Климента Ворошилова. 
  Когда Утёсов сказал, что «Одесский кичман» ему петь запрещено, последовало личное 
  разрешение «отца народов». И одна, и другая версии вполне правдоподобны. Во 
  всяком случае, и у Сталина, и у Чкалова (1) было уголовное прошлое. <br>
  <br>
  Кстати, у летчиков знаменитой эскадрильи, которые послужили прототипами для 
  героев фильма «В бой идут одни старики», «коронной» песней в их военном ансамбле 
  была вовсе не «Смуглянка», а именно «С одесского кичмана». Узнав об этом, певец 
  подарил им приобретенный на свои средства истребитель «Леонид Утесов». <br>
  <br>
  После Великой Отечественной войны чрезвычайно популярна была переделка утёсовской 
  песни — «С берлинского кичмана», где высмеивались поверженные фашисты. Но в 
  50-е — 60-е годы певцу вновь было запрещено исполнение песни. Его дело продолжил 
  Аркадий Северный, включивший песню в свой репертуар. <br>
  <br>
  Первоначальный, «каторжанский» вариант «Кичмана», к сожалению, неизвестен. Нынешний 
  текст — это смесь позднейших переделок и обработок. Явная вставка — о «герое 
  гражданской войны», «партизанском махновце», да и вообще вся тема гибели персонажей 
  в бою и на посту никак не вяжется с царской каторгой. <br>
