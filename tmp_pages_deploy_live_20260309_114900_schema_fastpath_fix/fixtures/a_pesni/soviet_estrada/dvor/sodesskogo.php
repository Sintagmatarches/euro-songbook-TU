  Намек на бытование песни в годы Гражданской войны есть в неоконченном, частично автобиографическом романе Александра Фадеева «Последний из удэге» (1929-1940) о приморских партизанах. Время действия — май 1919 года. Рабочий Яков Бутов и большевик Алеша Маленький спорят о том, надо ли начинать стачку на Сучанском руднике, занятом колчаковцами:<br>
  <br>
  <em>«Яков Бутов не был сторонником немедленной стачки. Он считал, что лучше было бы оттянуть выступление рабочих до того, как партизаны начнут новое наступление на рудник. Но так как все на руднике требовали немедленной стачки, и в отдельных шахтах вспыхивали самочинные забастовки, и вся многотысячная масса давила на рудничный комитет, обвиняя его в бездеятельности, трусости, а наиболее горячие даже в предательстве, и всем этим пользовались левые эсеры ми анархисты, то Яков Бутов так освещал положение дел на руднике, что получалось, что нужно или немедленно наступать на рудник или, если это даже невозможно, немедленно объявить стачку.

<br>
  <br>- Ишь, куда гнет!.. - раздувая свои широкие волосатые ноздри, посверкивая глазками, говорил Алеша Маленький. - До сих пор мы, грешные люди, думали, что надо идти в голове массы, учить ее, коли нужно, сдерживать ее. А у вас на руднике считают, видать, что надо плестись в заду у массы... В слабости своей расписываетесь, товарищи...
  
