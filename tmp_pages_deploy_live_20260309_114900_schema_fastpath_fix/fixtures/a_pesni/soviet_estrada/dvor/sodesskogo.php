  <br>
  <br>
  Вариант этой песни в 1928-29 гг. исполнялся в спектакле ленинградского Театра 
  сатиры &quot;Республика на колесах&quot; по пьесе Якова Мамонтова. Спектакль был 
  посвящен жизни железнодорожных воров. Главаря шайки Андрея Дудку (&quot;президента 
  республики на колесах&quot;) играл Леонид Утесов, и это была одной из его песен 
  в спектакле. В том же спектакле Утесов пел <a href="gopsosmykom.php">&quot;Гоп-со-смыком&quot;</a> 
  (к этому периоду творчества относятся и его <a href="bubliki.php">&quot;Бублики&quot;</a>). 
  Дальнейшему распространению способствовала запись песни Утесовым на грампластинку 
  в 1932 году. <br>
  <br>
  Первоначально в исполнении Утесова урканы бежали <em>&quot;с вапнярского кичмана 
  на Одест&quot;</em> (т.е. в Одессу), а затем он стал петь наоборот, - бежать 
  стали <em>&quot;с одесского кичмана&quot;</em> в <em>&quot;вапняровскую малину&quot;</em>. 
  Очевидно, в обоих случаях как один из пунктов путешествия имеется в виду станция 
  Вапнярка на границе нынешних Винницкой и Одесской областей. Бежав из вапнярской 
  тюрьмы, проще всего было укрыться в Одессе - ближайшем большом городе.<br>
  <br>
  &quot;С одесского кичмана&quot; Утесов исполнил также в дебютном выступлении 
  созданного им теа-джаза - на концерте в честь Международного женского дня в 
  Малом оперном театре Ленинграда 8 марта 1929 года. В дальнейшем он добавил к 
  песне &quot;соцреалистический&quot; куплет: <br>
  <br>
  <em>Одесса, Одесса,<br>
  Давно все это было,<br>
  Теперь звучат мотивы уж не те:<br>
  Бандитские малины давно уж позабыты<br>
  И вытеснил кичманы - новый быт.</em><br>
  <br>
  Член-корреспондент РАН и главный редактор журнала &quot;Вопросы языкознания&quot; 
  Татьяна Николаева вспоминает, что в ее детстве, в 1938-40 гг., текст песни был 
  более политическим:<br>
  <br>
  За что же мы боролись?<br>
   За что же мы сражались?<br>
    За что мы 
  проливали свою кровь?<br> Буржуи ликуют, буржуи пируют...<br>
  <br>
  <em>См. Т. Николаева. <a href="a-ruszen.php">Русская женщина в городском (блатном) 
  шансоне</a>.</em><br>
  <br>
  <br>
  В основе своей это старая каторжная песня, царских времен, навеянная песней 
  на стихотворение Генриха Гейне в переводе Михаила Михайлова <a href="../popular20/grenadery.htm">&quot;Из 
  Франции два гренадера&quot;</a>. Указанные в сборнике &quot;Запрещенные песни&quot; 
  Кельман и Тимофеев - не авторы, а в лучшем случае обработчики одного из вариантов. 
  Когда и при каких обстоятельствах (в Одессе?) в песню попал &quot;махновец партизанский&quot;, 
  непонятно. <br>
  <br>
  Существует очень много похожих песенок, связанных с событиями Первой мировой, 
  Гражданской, двумя польскими (1918-20 и 1939 гг.), финской 1939-40 гг. и Великой Отечественной войнами. Герои мирно возвращаются домой, и вдруг одного из них смертельно ранят 
  какие-нибудь неожиданные враждебные силы: охрана, немцы, поляки, финны. Почему-то, 
  очень часто финны - видимо, эхо тяжелой финской войны. Удар абсурден и нелогичен: &quot;Бабах! 
  и вот тебе, мил человек, злобный белофинн&quot;. С такой точки зрения, песня 
  &quot;Шли три армейца на финску границу&quot; 
  &quot;неправильная&quot; - герои там идут на войну, а должны бы уже возвращаться. 
  Умирая, герой произносит монолог типа: &quot;Товарищ, товарищ, болят мои раны...&quot;. 
