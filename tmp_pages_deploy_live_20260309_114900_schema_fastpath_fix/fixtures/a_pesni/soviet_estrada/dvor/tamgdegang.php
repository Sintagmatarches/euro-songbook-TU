<br>
<strong>3. Раджа</strong><br>
<br>
Там, где Ганг струится в океан,<br>
Там, где синий бледен небосвод, <br>
Там, где тигр крадется меж лиан,<br>
Там, где в джунглях бродит дикий слон,<br>
Там раджа гнетет <br>
Великан-народ,<br>
И звучит порой один напев,<br>
То поет индус, скрывая гнев:<br>
<br>
Эх, край велик Пенджаб,<br>
Там живет раджа,<br>
И порой его приказ<br>
Кровь и смерть несет для нас.<br>
Для своих пустых затей,<br>
Для своих пустых затей<br>
Славный свой народ<br>
Магараджа гнетет.<br>
<br>
Лесть придворных сделалась груба,<br>
И тоска властителя томит.<br>
- Эй, позвать ко мне сюда раба!<br>
Пусть хоть он меня развеселит.<br>
Юный раб предстал, <br>
И раджа сказал:<br>
- В верности мне все клялись не раз,<br>
Так исполни, раб, ты мой наказ.<br>
<br>
Край велик Пенджаб,<br>
Так велит раджа:<br>
Ту, которая милей,<br>
Для меня, мой раб, убей.<br>
Слово раджи - закон,<br>
Иначе ты будешь казнен.<br>

