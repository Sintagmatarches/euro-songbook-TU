Борис Фомин подразделял свои вокальные произведения 1920-х годов на романсы, русские песни и интимные песенки. &quot;Магараджа&quot; - одна из последних. К концу НЭПа Фомин  опубликовал, по меньшей мере, десять интимных песенок - именно столько значится, например, в перечне произведений, напечатанном на оборотной стороне нот романса <a href="../romans/fomin/nezdannym.php">&quot;Нежданным ураганом&quot;</a>, который Фомин выпустил за свой счет в 1929 году в Сухуми:<br>
<br> 
- 
&quot;Три маленьких розы&quot;, сл. неизв. автора; <br>
- &quot;Маленький Джони&quot;, сл. Веры Инбер;<br> 
- &quot;Вы плачете, детка&quot;, сл. Б. Кормилицына;<br>
- &quot;Маска смеха&quot;, сл. Константина Подревского;<br>
- &quot;Багдадский вор&quot;, сл. Константина Подревского;<br>
- &quot;Одалиска&quot;, сл. Михаила Гальперина; <br>
- &quot;Так просто - совсем легко&quot;, сл. Константина Подревского; <br>
- &quot;Магараджа&quot;, сл. Бориса Тимофеева; <br>
- &quot;Айзик&quot;, сл. Константина Подревского; <br>
- Таити-Трот&quot; (аранжировка), сл. Константина Подревского. <br>
