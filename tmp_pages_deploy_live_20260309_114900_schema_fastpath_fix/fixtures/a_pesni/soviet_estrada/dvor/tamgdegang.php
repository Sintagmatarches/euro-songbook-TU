<strong>ФОЛКЛОРИЗОВАННЫЕ ВАРИАНТЫ (3)<br>
<br>
1. Там, где Ганг впадает в океан</strong><br>
<br>
Там, где Ганг впадает в океан,<br>
Там, где голубеет небосклон, <br>
Там, где тигр крадется средь лиан<br>
И по джунглям бродит дикий слон,<br>
Там раджа гнетет великан-народ,<br>
И порой звучит один напев,<br>
То поет индус, скрывая гнев:<br>
<br>
Край велик Пенджаб,<br>
Там жесток раджа,<br>
И порой его приказ<br>
Смерть и кровь несет для нас.<br>
Для жены своей,<br>
Для пустых затей<br>
Славный свой народ<br>
Магараджа гнетет.<br>
<br>
Лесть придворных сделалась груба,<br>
И печаль властителя томит.<br>
- Эй, позвать ко мне сюда раба!<br>
Пусть хоть он меня развеселит.<br>
Юный раб предстал, раджа сказал:<br>
- Все вы в верности мне клялись не раз,<br>
Так исполни, раб, мой приказ.<br>
<br>
Край велик Пенджаб,<br>
Так велит раджа:<br>
В мире ту, кого<br>
Любишь всех сильней,<br>
Поди убей.<br>
Так тебе сказал,<br>
Так я приказал.<br>
Слово - закон,<br>
Или будешь ты казнен.<br>
<br>
Ждет три дня и три ночи весь Пенджаб.<br>
Ждет владыка, опершись о трон.<br>
Вот к нему приходит бледный раб,<br>
Чью-то голову приносит он.<br>
И глядит раджа на нее, дрожа,<br>
В ней знакомые черты нежны,<br>
В ней узнал лицо своей жены.<br>
<br>
Край велик Пенджаб!<br>
- Как велел раджа,<br>
В мире ту, кого любил,<br>
Для тебя твой раб убил.<br>
Так ты мне сказал,<br>
Так ты приказал.<br>
Верность слепа,<br>
Прими же дар раба.<br>
