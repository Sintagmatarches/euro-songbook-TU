- Чайка! Ты как пена морская чиста.<br>
- Чайка, белокрылая чайка, \ bis<br>
Черноморская чайка, моя мечта! /<br>
<br>
Я помню тот вечер багряный, лиловый<br>
Магнолий лиловых в цвету,<br>
И твой поцелуй горьковато-соленых,<br>
И бронзовых рук теплоту.<br>
