<br>
<strong>2. Чайка</strong><br>
<em>В ритме вальса </em><br>
<br>
В том городе южном, где синие дали,<br>
Где солнце, вода и тепло,<br>
Ее белокрылою чайкой прозвали,<br>
И имя ей то подошло.<br>
<br>
<em>Припев:</em><br>
<br>
Чайка! Повторяют невольно уста.<br>
Чайка! Ты как пена прибоя чиста.<br>
Чайка! Белокрылая чайка.<br>
Черноморская чайка. Моя мечта.<br>
<br>
Я помню тот вечер в долине зеленой…<br>
Магнолии в белом цвету,<br>
Ее поцелуй горьковато-соленый<br>
И бронзовых рук теплоту.<br>
<br>
Она улетела в даль синего моря,<br>
Ее не вернуть уже вновь,<br>
Доверчивой чайке расставила сети<br>
Другая большая любовь.<br>
<br>
С той славною чайкой расстались мы вскоре,<br>
С тех пор пронеслось много лет.<br>
Но снятся мне летом и чайки, и море,<br>
Хоть стал я давно уже дед.<br>
<br>
<em>В нашу гавань заходили корабли. Вып. 5. М., Стрекоза, 2001.</em><br>
 <br>
