	<span class="header"><strong>ЧАЙКА 
</strong><br></span>
<br>
Музыка Михаила Петренко<br>
Слова Николая Южного <br>
<br>
В том городе юном, где синие дали,<br>
Где воздух, цветы и тепло,<br>
Ее черноморскою чайкой прозвали,<br>
И имя ее подошло.<br>
<br>
<em>Припев:</em><br>
<br>
- Чайка! - повторяли невольно уста.<br>
- Чайка! Ты как пена прибоя чиста.<br>
- Чайка, черноморская чайка,<br>
Белокрылая чайка, моя мечта!<br>
<br>
Сердито смотрели вослед ей матросы,<br>
Когда на рыбачьем челне<br>
Она улетела в плохую погоду<br>
Навстречу гигантской волне.<br>
<br>
Я помню тот вечер в саду отдаленном,<br>
Полон магнолий, цветы,<br>
Один поцелуй горьковато-соленый<br>
И бронзовый цвет темноты.<br>
<br>
<em>Расшифровка фонограммы Бориса Рубашкина, аудиокассета «Любо, братцы,
любо…», «RDM», 1995.</em><br>
 <br>
<br>
Вышла на пластинке Ногинского завода в 1938 году под заглавием &quot;Черноморская чайка&quot; в исполнении тенора Павла Михайлова и джаз-оркестра под управлением Самуила Жака
(пластинка Г 9596, ГРК 2886).<br>
<br>
<img src="tchajkadisk.jpg" width="594" height="569"><br>
<br>
 Название &quot;Чайка&quot; носит также <a href="../romans/tchajka.php">популярный романс</a> Е. Жураковского 
на слова Елены Буланиной.<br>
<br>
<br>
<strong>ВАРИАНТЫ (3)<br>
<br>
1.</strong><br>
<br>
<img src="tchajka.gif" width="710" height="797"><br>
<br>
<br>
В том городе шумном, где синие дали,<br>
Где солнце, туман и тепло,<br>
Ее белокрылою чайкой назвали,<br>
И имя ей то подошло.<br>
<br>
<em>Припев:</em><br>
<br>
«Чайка, - повторяют невольно уста, -<br>
Чайка, ты, как пена прибоя, чиста.<br>
Чайка, белокрылая чайка,<br>
Черноморская чайка - моя мечта.<br>
Чайка, белокрылая чайка,<br>
Черноморская чайка - моя мечта».<br>
<br>
Сердито кричали матросы на вахте,<br>
Когда на большой высоте<br>
Она выходила в любую погоду<br>
Навстречу кипящей волне.<br>
<br>
Я помню свиданье в долине зеленой,<br>
Магнолии в белом цвету,<br>
Ее поцелуй горьковато-соленый<br>
И бронзовых рук теплоту.<br>
<br>
Она улетела на раннем рассвете,<br>
Ее не увидишь ты вновь:<br>
Доверчивой чайке расставила сети<br>
Другая – большая любовь.<br>
<br>
<em>Слова и музыка – не позднее 1957 года. За основу взята песня «Черноморская 
чайка» (слова М. Южного, музыка М. Петренко, не позднее 1941 года).<br>
<br>
Павлинов А. Т., Орлова Т. П. Ни пуха ни пера. Студенческие и туристские 
песни. СПб.: Издательство «Композитор» (Санкт-Петербург), 2000. - инициалы автора слов указаны неверно (&quot;М&quot; вместо &quot;Н&quot;).</em><br>
