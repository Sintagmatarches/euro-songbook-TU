	<span class="header"><strong>ДРЕ-ПА-ПЕШ</strong><br></span>
  <br>
  В далеком Лондоне погасли огоньки,<br>
  В далеком Лондоне все спать легли.<br>
  И только в баре, где все по паре,<br>
  Танцуют танец шеко-гали до зари.<br>
  <br>
  <em>Припев:</em><br>
  <br>
  А дре-па-пеш-пеш-пеш-пеш-пеш.<br>
  А дре-па-пульки-пульки-пульки-пульки-пульки...<br>
  А дре-па-пеш-пеш-пеш-пеш-пеш,<br>
  А дре-па-пульки-пульки-пульки, и назад.<br>
  <br>
  Один мальчишечка в углу сидит,<br>
  Его девчоночка с другим кутит.<br>
  Она кокетлива, ко всем приветлива,<br>
  А он всё курит, курит, курит и молчит.<br>
  <br>
  Пошел он в поле, нарвал букет.<br>
  А сам гадает: возьмет иль нет?<br>
  Она взяла цветы, расхохоталася,<br>
  И разбросала по паркету весь букет.<br>
  <br>
  Не плачь, мальчишечка, брось слезы лить,<br>
  Ведь нас, девчонок, нельзя любить.<br>
  Сегодня с этим, а завтра с третьим -<br>
  Вот так проходит наша молодая жизнь.<br>
  <br>
  В далеком Лондоне погасли огоньки,<br>
  В далеком Лондоне все спать легли.<br>
  И только в баре, где пьют вино,<br>
  Любовь там пропита и продана давно…<br>
  <br>
  <em>С фонограммы Владимира Меньшова, CD «В нашу гавань заходили корабли» 
  № 3, «Восток», 2001. После последнего куплета припев повторяется дважды.</em><br>
 <br>
  <br>
  Действие песенки происходит не только в Лондоне, встречаются и другие географические варианты - <a href="vdalekrio.php">&quot;В далеком Рио спят корабли...&quot;</a> 
  или, как приводит Анри Волохонский в статье <a href="piter/a-zabyt.php">&quot;Забытые 
  песни&quot;</a> (&quot;Митин журнал&quot;, №59, 2001), &quot;В далеком Ревеле&quot;, 
  причем возможным автором текста он называет Веру Инбер:<br>
  <br>
  <em>В далеком Ревеле погасли фонари <br>
  А в шумном баре зажглись огни <br>
  Играет джаз-банд, поет цыганка <br>
  И все танцуют модный шимми и фокстрот <br>
  <br>
  Один лишь мальчик в углу сидит <br>
  Его Жаннета с другим кутит <br>
  <br>
  Она приветлива и с ним кокетлива <br>
  А он сидит один в углу и все молчит <br>
  Зовет Жаннету он на фокстрот <br>
  Жаннета ручку ему дает... <br>
  <br>
  Мой милый мальчик... <br>
  Ведь в шумном баре нельзя любить <br>
  Ведь там где женщины и где вино <br>
  Любовь забыта уж давно! </em><br>
  <br>
  <br>
