	<span class="header"><strong>ЖЕНИТЬБА В ДОМЕ ШНЕЕРЗОНА</strong> <br></span>
  <br>
  Ужасно шумно в доме Шнеерзона, <br>
  Из окон прямо дым идет, <br>
  Там женят сына Соломона, <br>
  Который служит в «Капремонт». <br>
  <br>
  Его невеста Сонька с финотдела <br>
  Вся разодетая и в пух и прах: <br>
  Фату мешковую одела, <br>
  И деревяшки на ногах. <br>
  <br>
  Сам преддомком Абраша Дер-Молочник <br>
  Вошел со свитою — ну прямо царь! <br>
  За ним Вайншток — его помощник — <br>
  И Хаим Качкес — секретарь. <br>
  <br>
  А было их четыре брата вместе: <br>
  Найтула, Лёва, Хаим и Абрам. <br>
  Оставила мамаша им наследство, <br>
  А папа взял и умер сам. <br>
  <br>
  И вот на стол подали три графина: <br>
  Один — себе с лимонной кислотой, <br>
  Второй с раствором сахарина, <br>
  А третий сам-себе пустой. <br>
  <br>
  Найтула, Лёва, Хаим живут вместе. <br>
  Абрашу они ищут целый год. <br>
  Живет Абраша в городе Одессе, <br>
  Но как же он без них живет? <br>
  <br>
  И вот собралось все семейство разом: <br>
  Нашли себе жену на первый класс. <br>
  А вместо свадьбы стали джазом — <br>
  Играют кто во что горазд.<br>
  <br>
  Найтула, Лёва дуют на кларнете, <br>
  А Хаим, тот втыкает марафет, <br>
  Абрам бьет в барабан, как по котлете, <br>
  И получился у них квартет. <br>
  <br>
  Ох, а если б мама все это слыхала, <br>
  Ох, а если б мама все это слыхала, <br>
  Она бы в сумасшедший дом ушла. <br>
  Ох, папа бы их взял и застрелился, <br>
  Ну а потом сошел с ума! <br>
  ...Ой, что вы говорите?! Ужасно! <br>
  <br>
  Ужасно шумно в доме Шнеерзона, <br>
  Из окон прямо дым идет, <br>
