  <em>Блатная песня: Сборник. – М.: Изд-во ЭКСМО-Пресс, 2002. - без 
  указания авторов. </em><br>
<br>
  <br>
  Песня создана в 1920-м году одесским поэтом Мироном Ямпольским в своей квартире 
  №18 дома №84 на Канатной улице. На этот же мотив поются более поздние песни<a href="namoldavmuz.php"> 
  &quot;На Молдаванке музыка играет&quot;</a> о Беломорканале (там же см. ноты) 
  и <a href="vetcherngor.php">&quot;Вечерний город весь в электросвете&quot;</a>. 
  В представленном варианте нет сцены с мордобоем зарвавшегося Абраши, который учинил жених. <br>
  <br>
  <br>
  <strong>Из эссе Александра Розенбойма <a href="odessa/a-schneerson.php">&quot;Ужасно 
  шумно в доме Шнеерсона...&quot;</a> (Одесса, 2004):<br>
  </strong><br>
 &quot;Песни - это не отцовская многотомная библиотека или бабушкин громоздкий 
  буфет, их легко увезти потому, что в душе место всегда найдется. И разлетелись 
  одесские песни по разным странам, прижились там, и сегодня из какого-нибудь 
  ресторанчика, скажем, в Бруклине может вдруг донестись уже полузабытая у нас 
  &quot;Мине хотелось бы вам песни распевать бы...&quot; или &quot;Ужасно шумно 
  в доме Шнеерсона,/ Се тит зих хойшех прямо дым идет...&quot;<br>
  <br>
  Местные остряки когда-то не даром. утверждали, что знаменитая фраза Льва Николаевича 
  Толстого &quot;Все смешалось в доме Облонских&quot; в Одессе звучит не иначе, 
  как &quot;Се тит зих хойшех в доме Шнеерсона&quot;. По словам К. Паустовского, 
  эта песня &quot;обошла весь юг&quot;, а старожилы уверяли, что в начале регулярных, 
  многочисленных и подлежащих тогда &quot;безусловному посещению&quot; собраний 
  публика деревянными голосами пела &quot;Интернационал&quot;, но в конце отводила 
  душу &quot;Свадьбой Шнеерсона&quot; - если оно и неправда, то хорошо придумано. 
  Во всяком случае, когда 1 апреля 1999 года в Городском саду Одессы открывали 
  памятником водруженный на постамент бронзовый стул - один из двенадцати, разыскивавшихся 
  Остапом Бендером, то по окончании церемонии, выступлений официальных и не очень 
  официальных лиц вроде Михаила Михайловича Жванецкого, из специально установленного 
  по такому случаю динамика вовсю грянула &quot;Свадьба Шнеерсона&quot;, чему 
  свидетелем был и даю, как говорили в Одессе, &quot;голову на разрез&quot;. <br>
  <br>
  Действительно, песня побила рекорды популярности, долговечности и продолжительности 
  звучания: двенадцать куплетов этнографически точных реалий одесской жизни начала 
  1920 - х годов - &quot;Губтрамот&quot;, то есть губернский траспортный отдел, 
  &quot;деревяшки&quot;, сиречь, сандалии на деревянной подошве, в которых щеголяли 
  горожане вне всякой зависимости от пола и возраста, свадебный наряд из мешковины, 
  сахарин вместо сахара, а вместо чая &quot;гутеса сушеного настой&quot; - кипяток, 
  заваренный на сушеной айве...<br>
  <br>
  Звучит тут и музыкальное сопровождение свадьбы: &quot;На подоконнике три граммофона: 
  / Один с кеквоком бешено гудит,/ Тот жарит увертюру из &quot;Манона&quot;,/ 
  А третий шпильт дас фрейлехс ид&quot;. И это вовсе не гипербола, но самый что 
  ни на есть бесхитростный молдаванский шик, ради которого, к примеру, подгулявший 
  биндюжник мог прибыть домой &quot;на трех извозчиках&quot; - на первом восседал 
  он, на сиденье второго был небрежно брошен парусиновый балахон, на третьем покоился 
  картуз, как о том любит вспоминать одесский старожил В.С. Фельдман, библиограф, 
  который в книгохранилище своей университетской библиотеки чувствует себя, по 
  моему, комфортней, нежели в собственной квартире.<br>
  <br>
  А &quot;населяют&quot; песню старик Шнеерсон с сыном Соломоном, &quot;который 
  служит в Губтрамот&quot;, его &quot;невеста же курьерша с финотдела&quot;, гости, 
  танцующие &quot;все в угаре диком&quot;, безымянный дворник и &quot;сам преддомком 
  Абраша дер Молочник/ вошел со свитою, ну прямо просто царь/. За ним Вайншток, 
  его помощник,/ и Хаим Качкес - секретарь&quot;. Явившись непрошеным гостем и, 
  тем не менее, встреченный с полным молдаванским пиететом, преддомком ничтоже 
  сумняшеся &quot;налагает запрещенье&quot; на брак. И произошло то, что не могло 
  не произойти: &quot;Замашки преддомкома были грубы/ И не сумел жених ему смолчать./ 
  Он двинул преддомкома в зубы/ И начали все фрейлехс танцевать&quot;. Нужно сказать, 
  решительным мужчиной оказался Шнеерсон, поскольку в глазах тогдашних одесситов 
  преддомком, другими словами, председатель домового комитета, был персоной исключительной 
  значимости и даже опасности. Ведь именно он заверял всевозможные справки, имел 
  прикосновение к выдаче драгоценных хлебных карточек, надзирал за распределением 
  жилья, уже переименованного в жилплощадь... Помимо этого он &quot;брал на карандаш&quot; 
  содеянное и сказанное жильцами да &quot;постукивал&quot; на Маразлиевскую в 
  Губчека, коей командовал в то время Мендель Абелевич Дейч, одним из первых награжденный 
  орденом Красного Знамени, и одним из многих поставленный впоследствии к стенке 
  красной же властью. <br>
  <br>
  А одесский преддомком Абраша дер Молочник из песни &quot;Свадьба Шнеерсона&quot; 
  сродни своему московскому коллеге Швондеру из повести Михаила Булгакова &quot;Собачье 
  сердце&quot;, блестяще сыгранному потом одесситом Романом Карцевым в одноименном 
  фильме. Но если учесть, что песня появилась четырьмя годами раньше повести, 
  то первенство в гротескном изображении этого персонажа, рожденного грустной 
  эпохой, можно спокойно числить за Мироном Эммануиловичем Ямпольским, который 
  еще в 1920-м году написал &quot;Свадьбу Шнеерсона&quot; в квартире 18 дома №84 
  на Канатной улице - подробности нелишни, поскольку без них даже самая доподлинная 
  история со временем грозит обернуться легендой.<br>
  <br>
  Он был интеллигентным человеком с высшим образованием, состоял в Литературно-Артистическом Обществе, Союзе драматических и музыкальных писателей и в повседневной 
  жизни не изъяснялся на языке своей песни &quot;Свадьба Шнеерсона&quot;. Но он 
  прекрасно знал быт, обычаи, нравы, привычки, жаргон, фольклор одесского обывателя 
  и, к тому же, в начале 1920-х заведовал городским карточным бюро. А уж там перед 
  ним проходила &quot;вся Одесса&quot;, измученная революцией, гражданской войной, 
  интервенцией, национализацией, мобилизацией, контрибуцией, реквизицией, декретами, 
  уплотнениями, облавами, обысками, арестами, налетами, митингами и собраниями, 
  голодом и холодом, безжалостно, что называется по живому, разодранная на &quot;работающих&quot;, 
  &quot;неработающих&quot;, &quot;совслужащих&quot;, &quot;не совслужащих&quot;, 
  &quot;трудовой элемент&quot;, &quot;нетрудовой элемент&quot;...<br>
  <br>
  И двадцативосьмилетний Ямпольский написал &quot;Свадьбу Шнеерсона&quot;, искусно 
  стилизовав ее под фольклорную песню, каковой она, в сущности, и стала с годами, 
  совершенно &quot;оторвавшись&quot; от автора. Во всяком случае, в первом издании 
  любезной сердцам одесситов повести &quot;Время больших ожиданий&quot; К. Паустовский 
  уже приписал ее Я. Ядову, равно как и якобы появившуюся потом песню - продолжение 
  &quot;Недолго длилось счастье Шнеерсона&quot;, которая, в общем-то, вполне соответствовала 
  бы тому непредсказуемому, если не сказать сумасшедшему, времени. Но одесские 
  старожилы, знатоки и ревностные хранители прошлого, тотчас же забросали его 
  письмами с поправками, просьбами и категоричными требованиями восстановить имя 
  автора песни, неотделимой от Одессы их далекой молодости, как аромат старых 
  акаций на Малой Арнаутской улице после тихого майского дождя. А Изабелла Мироновна, 
  которая любезно разрешила мне когда-то переписать текст песни из чудом сохранившейся 
  у нее отцовской тетрадки, решительно отвергла версию о продолжении &quot;Свадьбы 
  Шнеерсона&quot;. И, поскольку все остальные, в частности, колоритная &quot;На 
  верху живет сапожник,/ На низу живет портной&quot; забылись, остался М.Э. Ямпольский, 
  по сути, автором одной песни&quot;.<br>
