<br>
Слова и музыка Александра Вертинского<br>
<br>
У меня завелись ангелята,<br>
Завелись среди белого дня!<br>
Всё, над чем я смеялся когда-то,<br>
Всё теперь восхищает меня!<br>
<br>
Жил я шумно и весело каюсь,<br>
Но жена всё к рукам прибрала.<br>
Совершенно со мной не считаясь,<br>
Мне двух дочек она родила!<br>
<br>
Я был против. Начнутся пеленки…<br>
Для чего свою жизнь осложнять?<br>
Но залезли мне в сердце девчонки,<br>
Как котята в чужую кровать!<br>
<br>
И теперь с новым смыслом и целью<br>
Я, как птица, гнездо свое вью<br>
И порою над их колыбелью<br>
Сам себе удивленно пою:<br>
<br>
«Доченьки, доченьки, доченьки мои!<br>
Где ж вы, мои ноченьки, где вы, соловьи?..»<br>
Вырастут доченьки, доченьки мои…<br>
Будут у них ноченьки, будут соловьи!<br>
<br>
Много русского солнца и света<br>
Будет в жизни дочурок моих.<br>
И, что самое главное, это<br>
То, что Родина будет у них!<br>
<br>
Будет дом. Будет много игрушек,<br>
Мы на елку повесим звезду…<br>
Я каких-нибудь добрых старушек<br>
Специально для них заведу!<br>
<br>
Чтобы песни им русские пели,<br>
Чтобы сказки ночами плели,<br>
Чтобы тихо года шелестели,<br>
Чтобы детства забыть не могли!<br>
<br>
Правда, я постарею немного,<br>
Но душой буду юн, как они!<br>
И просить буду доброго Бога,<br>
Чтоб продлил мои грешные дни!<br>
<br>
Вырастут доченьки, доченьки мои…<br>
Будут у них ноченьки, будут соловьи!<br>
А закроют доченьки оченьки мои –<br>
Мне споют на кладбище те же соловьи!<br>
