        <br>
        <strong> Николай Александрович Щорс</strong> (1895-1919) - &quot;народный 
        герой&quot; Гражданской войны на стороне красных. Практически все они 
        либо погибли на полях войны, как Василий Чапаев, либо были ликвидированы 
        в ее ходе по команде большевицкого руководства, так как ему по разным 
        причинам мешали. Смерть часто списывалась на врагов - так был застрелен 
        и сам Щорс, - либо их устранили вскоре после Гражданской, как застрелили 
        Филиппа Миронова и Григория Котовского. Редкие персонажи дожили до 1937 
        года, подобно Блюхеру.<br>
        <br>
        Военный фельдшер Николай Щорс Первую мировую войну окончил в чине поручика. 
        Октябрьский переворот не поддержал. Тем не менее, в 1918 году числился 
        в левоэсеровском Курском полку. После ликвидации левых эсеров обратился 
        к большевикам (мотивы выдвигают разные, в том числе любовь к чекистке 
        Фруме Хайкиной, которая уже после смерти Щорса родила ему дочь Валентину). 
        Организовал на Украине красный партизанский отряд для борьбы с немцами, 
        разросшийся до 1-го украинского советского полка им. товарища Ивана Богуна 
        (Иван Богун - сподвижник Богдана Хмельницкого), а затем - до дивизии. 
        Показал себя талантливым командиром. При этом был в крайне плохих отношениях 
        с Реввоенсоветом Республики (т. е. с Кремлем), обвинялся в &quot;партизанщине&quot;. 
        30 августа 1919 года во время командования боем с петлюровцами был застрелен 
        сзади из револьвера в голову, предположительно, чекистом Павлом Танхиль-Танхилевичем. 
        Согласно медэкспертизе при эксгумации тела в 1949 году убит был с расстояния 
        10-15 метров. Был вычеркнут из официальной истории Гражданской войны. 
        Возвращен в нее в середине 1930-х как пример &quot;советизации Украины 
        снизу&quot;. В 1935 году Александр Довженко получил от Сталина заказ на 
        фильм &quot;Щорс&quot;, в 1939 фильм закончен. Консультант фильма, замначальника 
        дивизии Щорса Иван Дубовой, на глазах которого Щорс погиб, выхода фильма 
        не дождался - был репрессирован в 1937 году. Сцены гибели Щорса в фильме 
        нет. <br>
        <br>
        <br>
        Поэт <strong>Михаил Голодный</strong> (1903-1949) - очевидец Гражданской 
        войны на Украине. До 1920 года жил в Екатеринославе (ныне Днепропетровск), 
        где в 1919 году вступил в комсомол. Затем переехал в Харьков, столицу 
        Советской Украины. В начале 1920-х гг. вместе с екатеринославскими комсомольскими 
        приятелями - начинающими поэтами Михаилом Светловым (будущим автором <a href="grenada.php">&quot;Гренады&quot;</a>) 
        и Александром Ясным - уехал учиться литературе в Москву. Автор слов популярных 
        песен о Гражданской войне <a href="ptchapajevtsa.php">&quot;Песня чапаевца&quot;</a>, 
        &quot;Песня о Щорсе&quot; и <a href="zelezniak.php">&quot;Партизан Железняк&quot;</a>.<br>
