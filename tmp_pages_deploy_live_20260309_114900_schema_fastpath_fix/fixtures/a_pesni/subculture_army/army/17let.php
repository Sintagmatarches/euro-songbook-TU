        В семнадцать лет, еще мальчишкой,<br>
        В пилоты он служить ушел.<br>
        В стальной машине со звездой на крыльях<br>
        Утеху он себе нашел.<br>
        <br>
        Писал он часто: «Скоро приеду,<br>
        А как приеду, так обниму».<br>
        «Я буду ждать, чтоб ни случилось», -<br>
        Так отвечала она ему.<br>
        <br>
        Но вот однажды порой ненастной<br>
        Письмо пришло к нему издалека.<br>
        Со злобной шуткой друзья писали,<br>
        Что «больше не любит она тебя».<br>
        <br>
        «Ну что ж? Не любит - так и не надо!<br>
        За что же я ее люблю?!<br>
        Мне только стоит подняться в небо<br>
        И сделать “мертвую петлю”».<br>
        <br>
        Ну что ж? Не любит – так и не надо!<br>
        И для петли он руль нажал.<br>
        На высоте трех тысяч метров<br>
        Пропеллер яростно жужжал.<br>
        <br>
        Вот самолет за дальним лесом<br>
        На полной скорости упал.<br>
        Пилот в крови весь, с разбитой грудью<br>
        Губами бледными шептал:<br>
        <br>
        «Так, значит, амба. Так, значит, крышка.<br>
        Настал любви моей последний час.<br>
        Тебя любил я еще мальчишкой.<br>
        Еще сильнее люблю сейчас».<br>
        <br>
        А в этот вечер она мечтала:<br>
        Что вот вернется любимый мой…<br>
        А через час она узнала:<br>
        Погиб пилот ее родной.<br>
        <br>
        «Ну что ж? Погиб он – и я погибну», -<br>
        Решила девушка тогда.<br>
        И в тот же вечер в речные волны<br>
        С обрыва бросилась она.<br>
