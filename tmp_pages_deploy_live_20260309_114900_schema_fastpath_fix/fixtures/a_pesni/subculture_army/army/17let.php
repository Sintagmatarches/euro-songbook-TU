        С машиной быстрой, с звездой на крыльях<br>
        Себе утёху он нашел.<br>
        <br>
        Они любили друг друга крепко,<br>
        Когда еще были детьми.<br>
        И часто-часто порой шептали,<br>
        Что не забудем друг друга мы.<br>
        <br>
        Но вот письмо ему прислали<br>
        Его хорошие друзья,<br>
        Что изменила твоя малютка,<br>
        Что уж не любит она тебя сейчас.<br>
        <br>
        - Ну что ж, не любит, так и не надо!<br>
        Зачем же я ее люблю?<br>
        И что мне стоит с машиной быстрой<br>
        Не сделать мертвую петлю?<br>
        <br>
        На высоте пять тысяч метров<br>
        Как ветер весело жужжа,<br>
        Пилот в крови весь с измятой грудью<br>
        Губами медленно шептал:<br>
        <br>
        - Так значит крышка! Так значит амба!<br>
        Любви настал последний час.<br>
        Тебя любил я еще мальчишкой<br>
        И еще больше люблю тебя сейчас!<br>
