        <span class='bold'>2. Они любили друг друга с детства</span><br>
        <br>
        Они любили друг друга с детства,<br>
        В пилоты он хотел пойти,<br>
        В машине звездной подняться в небо,<br>
        И счастье там свое найти.<br>
        <br>
        А высота пятьсот пятнадцать,<br>
        Пропеллер весело гудит,<br>
        «Я знаю любит меня родная», -<br>
        Так сердце радостно стучит.<br>
        <br>
        Друзья узнали и написали:<br>
        «Она не любит уж тебя».<br>
        Друзья писали со злобной шуткой:<br>
        «Она другому отдана».<br>
        <br>
        «Ну что ж, не любит, так и не надо,<br>
        Зато ее я так люблю,<br>
        Ну что мне стоит подняться в небо<br>
        И сделать мертвую петлю».<br>
        <br>
        А высота три тыщи метров,<br>
        Пропеллер весело жужжал.<br>
        «Ну что ж, не любит, так и не надо!»<br>
        И на штурвал рукой нажал.<br>
        <br>
        Машина с грохотом вонзилась в землю,<br>
        Пропеллер сразу замолчал,<br>
        Красивый мальчик с разбитой грудью<br>
        Губами бледными шептал:<br>
        <br>
        «Ну что ж, не любит, так и не надо,<br>
        Зато ее я так люблю,<br>
        Из-за нее я поднялся в небо<br>
        И сделал мертвую петлю».<br>
        <br>
        Друзья узнали, похоронили,<br>
        Пропеллер сделали крестом,<br>
        И часто-часто на той могиле<br>
        Девчонка плакала о нем.<br>
        <br>
        Но вот однажды в большую бурю<br>
        С обрыва бросилась она.<br>
        Вот так погибла возлюбленная пара,<br>
        А виноваты всё друзья!<br>
