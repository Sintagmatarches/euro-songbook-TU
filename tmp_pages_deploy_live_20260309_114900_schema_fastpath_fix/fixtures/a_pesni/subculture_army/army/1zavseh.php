	<h1>ОДИН ЗА ВСЕХ И ВСЕ ЗА ОДНОГО</h1>
	<br>
  Полковая песня Л.-гв. Конного полка<br><br>
  Музыка А.В. Ружека<br>
  Слова Л.-гв. штабс-ротмистра барона П.Н. Врангеля<br>
  <br>
«Один за всех», не оглядясь,<br>
И «все за одного» тотчас — <br>
Заветы таковы полка.<br>
В них полк уже живет века,<br>
И в дни тяжелые невзгод <br>
Пусть каждый, как один встает <br>
И мчится вмиг во весь опор <br>
К штандарту по сигналу «сбор»!<br>
<br>
<em>Хор:</em><br>
<br>
Конногвардейцы, ко мне! <br>
Быстро несись на коне, <br>
Коли живей, руби сильней, <br>
Врага рази...
<br>
<br>
В огонь не раз наш полк ходил:
<br>
Он шведа под Полтавой бил,
<br>
В исторью целый ряд страниц <br>
Полк записал: Аустерлиц,<br>
И Фридланд, и Бородино <br>
Известны каждому давно,<br>
И на полях жестоких сеч<br>
Не раз пришлось костьми нам лечь...<br>
<br>
<em>Хор.</em><br>
<br>
Присягу верности храня,<br>
В дни смут полк не слезал с коня, <br>
Всегда готовый день и ночь <br>
Царю и родине помочь.<br>
В молчаньи грозном, как гроза, <br>
Крамоле полк смотрел в глаза,<br>
Готовый ринуться вперед,<br>
Лишь час желанный подойдет.<br>
<br> 
<em>Хор. </em><br><br>
«Один за всех», не оглядясь, <br>
И «все за одного» тотчас — <br>
Заветы таковы полка.<br>
В них полк уже живет века,<br>
И мощный славой боевой <br>
И дружбой крепкой, вековой, <br>
Полк ожидает новых сеч, <br>
Чтоб за Царя и Русь  полечь!<br>
<br>
<em>Хор.<br></em>
