	<br>
  Слова Федора Глинки<br>
  <br>
  Скоро зов послышим к бою <br>
  И пойдем опять вперед; <br>
  Милорадович с собою <br>
  Нас к победам поведет! <br>
  <br>
  Над дунайскими брегами <br>
  Слава дел его гремит; <br>
  Где ни встретится с врагами, <br>
  Вступит в бой - врагов разит. <br>
  <br>
  Вязьма, Красный, Ней разбитый <br>
  Будут век греметь у нас; <br>
  Лавром бед его обвитый <br>
  Бухарест от бедствий спас. <br>
  <br>
  Чтоб лететь в огни, в сраженье <br>
  И стяжать побед венец, <br>
  Дай одно лишь мановенье, <br>
  Вождь полков и вождь сердец! <br>
  <br>
  Друг солдат! служить с тобою <br>
  Все желанием горят; <br>
  И, к трудам готовясь, к бою, <br>
  Общим гласом говорят: <br>
  <br>
  "Милорадович где с нами, <br>
  Лавр повсюду там цветет; <br>
  С верой, с ним и со штыками <br>
  Русский строй весь свет пройдет!.." <br>
<br>
 <span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006. С прим.:<br>
  <br>
&quot;Вариант "Авангардной песни" Федора Глинки (музыка Д. Кашина), входивший 
  во многие солдатские песенники XIX века. Перечислены лишь некоторые из походов 
  и побед Милорадовича в русско-турецких войнах на Балканах при Суворове, освобождение 
  Бухареста в 1806 году и в Отечественной войне 1812 года - сражение под Вязьмой, 
  разгром войск маршала Нея под Красным. Уже при жизни его называли "героем времени, 
  любимцем армии и народа". Слова песни "Друг солдат! служить с тобою / Все желанием 
  горят" не были преувеличением. Уцелевший в 52 сражениях, он был смертельно ранен 
  14 декабря 1825 года на Сенатской площади. На уговоры не выезжать к бунтовщикам 
  он ответил: "Что же это за генерал-губернатор, если он боится пролить свою кровь, 
  когда кровопролитие неизбежно". Когда врач извлек пулю, Милорадович с облегчением 
  вздохнул: "Слава Богу, эта пуля не солдатская. Теперь я совершенно счастлив".&quot;<br>
  <br></span><br>
В приведенном выше комментарии смешиваются две песни - <a href="avangardp.php">"Авангардная песнь"</a> (&quot;Друзья! Враги грозят нам боем...&quot;), сложенная 4-стопным ямбом - размером, который в начале XX в. не был популярен в солдатской среде, и "Авангардная песня" (&quot;Скоро зов послышим к бою...&quot;), сложенную 4-стопным хореем, самым популярным размером солдатских песен с конца XVIII до начала XX в.<br>

