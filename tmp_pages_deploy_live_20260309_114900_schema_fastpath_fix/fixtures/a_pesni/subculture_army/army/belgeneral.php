 <br>
 Песня на смерть генерала Михаила Дмитриевича Скобелева (1843-1882), участника завоевания Средней Азии и русско-турецкой войны 1877-78 гг. За пристрастие к белым мундирам и белым коням он получил прозвище &quot;Белый генерал&quot; (у турков - &quot;Ак-Паш<strong>а</strong>&quot;). Собственно, сам Скобелев не был особо актуален на момент выхода этого песенника в 1907 г.: умер он 15 лет назад, только что закончилась русско-японская война, которая в песнях затмила прежние войны с их полководцами.  Однако в ноябре 1904 г. сестра Скобелева княгиня Н.Д. Белосельская-Белозерская основала &quot;Скобелевский комитет&quot; (&quot;Комитет имени генерал-адъютанта М.Д. Скобелева для выдачи пособий потерявшим на войне способность к труду воинам&quot;). Комитет стал крупнейшей российской благотворительно организацией для бывших военных и просуществовал до апреля 1918 г. Благодаря Комитету имя генерала оставалось на слуху.<br>
 
