		<h1>БЕЛЫЙ ГЕНЕРАЛ</h1>
	<br>
    Смерть Россию прослезила,<br>
Воеводу поразила<br>
В молодые годы:<br>
Он с врагами люто дрался,<br>
Бранной смерти не боялся,<br>
Совершал походы.<br>
За Балканские пределы<br>
Он ходил с могучей силой.<br>
С жаждою победы.<br>
Брал живьем он турок диких;<br>
Смельчаков таких великих<br>
Не запомнят деды.<br>
Как орел могучий в воле,<br>
Он летал на бранном поле,<br>
С бодростью, весельем,<br>
Сам всегда вперед солдата;<br>
Он солдата звал все братом,<br>
С ним шутил в безделье.<br>
А коней его могучих<br>
Под ружьем трескучим<br>
Много погибало.<br>
Смерть ему одно мгновенье!..<br>
Только Божье Провиденье<br>
Воина спасало.<br>
Турки все его боялись<br>
Сами, бедные, сознались,<br>
Как они несмелы.<br>
Как штыком их угощали,<br>
Как от слова трепетали:<br>
«Едет воин белый»!<br>
Богатырь удалый, бравый,<br>
Он вернулся с громкой славой,<br>
Ангелом хранимый;<br>
А теперь, к тоске несносной,<br>
Помер он победоносный,<br>
Генерал любимый.<br>
	<br>
