	<h1>БРОНЯ КРЕПКА, А ТРАНСПОРТЕРЫ БЫСТРЫ</h1>
Броня крепка, а транспортеры быстры,<br>
Огонь силён, а мёты хороши.<br>
Спасайтесь, чан- и прочие кайшисты -<br>
Наш Со- силен, а Юз - несокрушим!<br>
<br>
Гремя огнём, сверкая блеском стали,<br>
Пойдут машины в яростный поход,<br>
Когда отдаст приказ товарищ Сталин,<br>
Климент Ефремыч в бой нас поведёт!<br>
<br>
Наш ОЗК не пропускает газы,<br>
ВПХР спасает от ОВ,<br>
Наш АКМ не отказал ни разу,<br>
А наш ПМ страшней, чем ОМП.<br>
<br>
Гремя огнем, через посевы гречки<br>
Пойдут машины в яростный поход,<br>
Когда нам даст приказ товарищ Гречко,<br>
Семён Михалыч в бой нас поведет.<br>
<br>
Само- собой, и лёты есть не хуже,<br>
Сверхвысоко- и звуковые есть,<br>
И много скоро- разных стрельных пушек<br>
И бое- к ним головок нам не счесть.<br>
<br>
Гремя огнем, сквозь реки и пустыни<br>
Пойдут машины в яростный поход,<br>
Когда приказ объявит нам Устинов,<br>
И маршал Брежнев в бой нас поведет.<br>
<br>
Пред нами Пен трепещут и Тагоны,<br>
Пред нами Бунд не выстоит и Свер.<br>
Ни поли не оставим мы, ни гонов<br>
За наш СС, за родину СР<br>
<br>
Гремя огнем, развёртывая знамя,<br>
Пойдут машины в яростный поход,<br>
Когда нам даст приказ, а кто не знаем...<br>
И кто не знаем в бой нас поведёт!
