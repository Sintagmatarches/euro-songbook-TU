	<h1>ДЕЛИБАШ 
</h1>
<br>
Слова Александра Пушкина<br>
<br>
Перестрелка за холмами; <br>
Смотрит лагерь их и наш; <br>
На холме пред казаками <br>
Вьется красный делибаш. <br>
<br>
Делибаш! не суйся к<em> лаве</em>,<br>
Пожалей свое житье; <br>
Вмиг аминь лихой забаве: <br>
Попадешься на копье. <br>
<br>
Эй, казак! не рвися к бою: <br>
Делибаш на всем скаку <br>
Срежет саблею кривою <br>
С плеч удалую башку. <br>
<br>
Мчатся, сшиблись в общем крике. <br>
Посмотрите! каковы?.. <br>
Делибаш уже на пике, <br>
А казак без головы. <br>
<br>
1829 <br>
<br>
