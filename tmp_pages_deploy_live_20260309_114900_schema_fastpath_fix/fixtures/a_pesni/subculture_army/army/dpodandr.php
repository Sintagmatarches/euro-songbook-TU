	<h1> 
ДЕЛО ПОД АНДРЕЕВОЙ ДЕРЕВНЕЙ (23 апреля 1844 г.)</h1>
	Песня 80-го Пехотного Кабардинского полка 
	<br>
	<br>
	Звук победы раздается<br>
  Здесь в погибельных горах.<br>
  Где мы встретимся с врагами,<br>
  Мы сразимся на штыках.<br>
	<br>Мы спокойных дней не знаем,<br>
  Всегда ходим по горам.<br>
  Злых врагов казнить умеем,<br>
  Честь и слава будет нам.<br>
	<br>За нас Мать наша Россия<br>
  Молит Бога завсегда,<br>
  Нам от горского злодея<br>
  Нет покоя никогда.<br>
	<br>Двадцать третьего апреля<br>
  На рассвете бела дня,<br>
  С горя песенку запели<br>
  Про неверна Шамил<strong>я</strong>.<br>
	<br>Всех чеченцев и тавлинцев<br>
  Пособрал он со всех гор,<br>
  Во «Внезапной» Кабардинцев<br>
  Обступил со всех сторон.<br>
	<br>Нам стоять было опасно:<br>
  Войска множество его.<br>
  Ожидали ежечасно<br>
  Мы героя своего.<br>
	<br>Генерал-майор Козловский,<br>
  Закавказский наш герой<br>
  Знаем дух его геройский,<br>
  С ним снесем мы все долой!<br>
	<br>Гром гремит, земля трясется,<br>
  Запылали в нас сердца.<br>
  Конь, как лев, под ним несется –<br>
  Мы узнали тут отца!<br>
	<br>Вдруг увидел он чеченцев,<br>
  Свою шашку обнажил,<br>
  Да и с горстью Кабардинцев<br>
  Врага тут же порешил!<br>
	<br>
	<span class='i'>CD «Песенная летопись Кавказской войны». Исп. Мужской хор Института 
  певческой культуры «Валаам», дирижер И. Ушаков, запись 1997 г. СПб.: Русская 
  Лира, 2002.</span><br>
	<br><br>
  <em>23 апреля 1844 г.</em> - по старому стилю. <em>Внезапная</em> - русская 
  крепость на Сунженской линии.<br>
  <br>
  <br>
  Куплет <em>«Двадцать третьего апреля На рассвете бела дня»</em> вошел и в последующие 
  песни Кабардинцев, с этой датой уже ничем не связанные, кроме песенной традиции 
  - см. <a href="vtorzchamila.php">«Вторжение Шамиля в Кабарду»</a> (1846, на 
  другой мотив). Так часто бывает в фольклоре: например, дата &quot;21 сентября&quot; 
  путешествовала по песням кубанских казаков от Крымской войны 1853-56 гг. или ранее (<a href="../kazaki/kuban/a21sent.htm">&quot;А двадцать 
  первого, ой да, сентября...&quot;</a>) до Великой Отечественной 1941-45 гг. (<a href="../kazaki/kuban/ehdavspom.htm">&quot;Эх, 
  да вспомним, братцы...&quot;</a>).<br>

