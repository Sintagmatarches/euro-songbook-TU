	<h1>ЁЛОЧКА 
МИЛИТАРИСТСКАЯ</h1>

В лесу родилась ёлочка,<br>
В лесу она росла.<br>
Она для маскировочки<br>
Посажена была.<br>
<br>
А ёлочка - не ёлочка,<br>
Ракетный полигон.<br>
И каждая иголочка<br>
По сорок мегатонн.<br>
<br>
Ефрейтор в шубке заячьей<br>
Ухаживал за ней,<br>
Машинным маслом смазывал:<br>
"Смотри, не заржавей!"<br>
<br>
Летела в небе звёздочка<br>
Из дальних-дальних стран<br>
И много-много гадостей<br>
Под крыльями несла.<br>
<br>
Нажал майор на кнопочку<br>
(Простите, на сучок) -<br>
В звезду вонзилась ёлочка<br>
По самый корешок.<br>
<br>
Когда упала звёздочка,<br>
Желанье загадал,<br>
Чтоб в мире мир повсюду был,<br>
Чтоб атом мирно спал!<br>
