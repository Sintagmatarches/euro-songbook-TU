	<h1>ГЕНЕРАЛ ЕРМОЛОВ НА КАВКАЗЕ (1818 г.)</h1>
Русская солдатская песня <br>
<br>
Что да не лебедушки, в холод, дождь и стужу,<br>
  Весело солдатушки переплыли Сунжу.<br>
<br>
<em>Припев:</em><br>
  По всему Кавказу про нас слава ходит,<br>
  Наш «дедушка», наш Ермолов на всех страх наводит.<br>
<br>
Чуть лишь отряхнулися, как горы заняли,<br>
  С горцами столкнулися, Параул забрали.<br>
<br>
Все его покинули, ушли к Дженгутаю,<br>
  И там все прихлынули к своему Султану.<br>
<br>
Скоро овладели мы грозными стенами –<br>
  Бросились бежать тут тьмы лезгин пред нами.<br>
<br>
А потом, как поддали погорячей пара,<br>
  Словно как метлой смели самого Султана.<br>
<br>
<span class='i'>CD «Песенная летопись Кавказской войны». Исп. Мужской хор Института 
певческой культуры «Валаам», дирижер И. Ушаков, запись 1997 г. СПб.: Русская Лира, 
2002.</span><br>
<br>
<br>
Песня датирована ошибочно, так как описываемые события произошли в 1819-1820 гг. 
Параул и Дженгутай, главные селения ханства Мехтули в Дагестане, были взяты Ермоловым 
в ноябре 1819 г., и само ханство ликвидировано. В экспедиции участвовало 5 батальонов 
пехоты, 300 казаков и 13 орудий. В следующем 1820 г. Ермолов начал строительство 
на Сунженской линии напротив Эндери новой крепости Внезапная. В сентябре того 
же года Аварский султан Ахмад-хан с 6000 горцев атаковал русские войска, занятые 
строительством. Русские разбили аварцев, низложили султана и на его место поставили 
его сына - джанку Сухая. Восстания окрестных племен в ответ на возведение русскими 
крепостей были обычным явлением в ходе всей Кавказской войны 1817-1864 гг.<br>

