<br>
Порочная любовь</span> <br>
<br>
Губ твоих накрашенных малина, <br>
  В кольцах пальцы ласковой руки. <br>
  От бессонницы и кокаина <br>
  Под глазами черные круги.  <br>
<br>
<em>Припев: </em><br>
<br>
Муж твой в далеком море <br>
  Ждет от тебя привета, <br>
  В суровом ночном дозоре <br>
  Шепчет: «Где ты? Где ты?» <br>
<br>
Зубки твои в чувственном оскале, <br>
  Тонкая, изломанная бровь. <br>
  Слишком многие тебя ласкали, <br>
        Чтоб мужскую знала ты любовь. <br>
<br>
Офицеров знала ты немало — <br>
  Кортики, погоны, ордена... <br>
  О такой ли жизни ты мечтала, <br>
        Трижды разведенная жени? <br>
<br>
У любви порочной ты у власти. <br>
  И тогда, послушны и легки, <br>
  Цепенеют в пароксизме страсти <br>
        Пальчики изнеженной руки. <br>
<br>
Наполняясь, звякнули бокалы. <br>
  На подушку капли уронив, <br>
  Сброшенный мужской рукой усталой, <br>
        Шлепнулся на пол презерватив. <br>
<br>
Лишь порою встанешь ты не рано <br>
  С грустью от загубленной красы... <br>
  А на вечер — снова рестораны, <br>
  И снимаешь тонкие трусы.<br>
        <br>
Отошли в небытие притоны <br>
  Легких девок в наши времена, <br>
  Но верна велению закона <br>
        Чья-нибудь хорошая жена. <br>
