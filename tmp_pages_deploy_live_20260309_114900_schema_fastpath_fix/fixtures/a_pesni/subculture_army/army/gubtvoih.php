	<h1>ГУБ 
ТВОИХ НАКРАШЕННЫХ МАЛИНА...</h1>
<br>
Губ твоих накрашенных малина,<br>
В кольцах пальцы ласковой руки.<br>
От бессонницы, от кокаина<br>
Под глазами темные круги.<br>
Зубки твои в чувственном оскале,<br>
Тонкая изломанная бровь.<br>
Слишком многие тебя ласкали,<br>
Чтоб мужскую знала ты любовь.<br>
<br>
<em>Припев:</em><br>
<br>
А муж твой далеко в море<br>
Ждет от тебя привета,<br>
В далеком суровом дозоре<br>
Шепчет он: - Где ты?<br>
<br>
У любви порочной ты во власти,<br>
И тогда, послушны и легки,<br>
Цепенеют в пароксизме страсти<br>
Пальчики изнеженной руки.<br>
<br>
Офицеров знала ты немало – <br>
Кортики, погоны, ордена;<br>
О такой ты жизни и мечтала,<br>
Трижды развращенная жена.<br>
<br>
Отошли в небытие притоны<br>
Легких девок в наши времена,<br>
Но следует велению закона<br>
Чья-нибудь хорошая жена.<br>
<br>
        <span class='i'>В нашу гавань заходили корабли. Пермь: Книга, 1996.<br>
