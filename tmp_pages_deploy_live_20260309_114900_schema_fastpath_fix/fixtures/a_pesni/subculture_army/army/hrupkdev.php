	<h1>ХРУПКАЯ 
ДЕВОЧКА</h1>
<br>
Сколько раз я видел это, сколько раз?<br>
В кассах очередь - билетов пробил час.<br>
Отъезжают, провожают кто куда.<br>
Люди, лица исчезают, пролетают города.<br>
Ожидаю, скоро тронет, и курю.<br>
О своем, на посторонних не смотрю.<br>
Но сюжет обыкновенный тронет грудь,<br>
Если провожал военных кто-нибудь.<br>
<br>
        <em>Припев:</em><br>
<br>
Хрупкая девочка на перроне,<br>
Переживая, сжимает ладони.<br>
А из окна в важном лике отца<br>
Стриженый чуб офицера-юнца.<br>
Хрупкая девочка на перроне,<br>
Скорый наверное скоро тронет<br>
И унесет в незнакомую даль<br>
Преданность, верность, тоску и печаль.<br>
<br>
Дни до отпуска считали усачи.<br>
Отпуск первый отгуляли, засучи.<br>
Поезд скорый неизменный, а она<br>
Вдруг поймет, что муж военный и одна, <br>
И вновь одна.<br>
Ехать ей нельзя - учеба целый год.<br>
Ах, как долго ждать. И вот,<br>
Наконец, дела отпустят, и примчит,<br>
Даже если в доме пусто, даже если нет свечи.<br>
<br>
        <em>Припев.</em><br>
<br>
Извини меня, родная, вот билет.<br>
Я все прекрасно понимаю, не секрет.<br>
А ты все та же - прелесть, милость, взгляд смешной.<br>
Вот только чубчик твой покрылся редкой сединой.<br>
А помнишь, так же…<br>
<br>
        <em>Припев (в прошедшем времени).<br></em>
