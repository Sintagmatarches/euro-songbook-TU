	<h1>ИЗ-ЗА ЛЕСА, ЛЕСА ТЕМНОГО</h1>
	<br>
	Из-за леса, леса темного 
  <br>
  Из-за гор ли, гор высокиих <br>
  Тут бежит-спешит молодой солдат, <br>
  Молодой солдат, строевой сержант. <br>
  На головушке он указ несет, <br>
  Во правой руке — челобитьице, <br>
  У ключа, ключа у гремячего, <br>
  У колодезя у студеного <br>
  Добрый молодец коня поил,<br>
  Красна девица воду черпала, <br>
  Воду черпала, речь говорила: <br>
  «Ох ты душечка, молодой солдат! <br>
  Молодой солдат, строевой сержант! <br>
  Ты возьми, возьми за себя меня», <br>
  — «Ох ты глупая красна девица, <br>
  Неразумная дочь отецкая! <br>
  Мне нельзя тебя за себя взяти, <br>
  Мы вечор поздно со бою пришли, <br>
  Уж мы билися-рубилися, <br>
  Трое суточки не пиваючи, <br>
  Не пиваючи, не едаючи, <br>
  Со добрых коней не слезаючи, <br>
  Наши ноженьки подогнулися, <br>
  Белы рученьки опустилися, <br>
  Сабли вострые притупилися!» <br>
  <span class='i'><br>
  Русские народные 
  песни. / Сост. и вводн. тексты В.В. Варгановой. М.: Правда, 1988. Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания».</span><br>

