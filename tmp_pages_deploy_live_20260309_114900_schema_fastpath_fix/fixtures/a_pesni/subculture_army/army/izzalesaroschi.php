	<h1>ИЗ-ЗА 
ЛЕСА, ИЗ-ЗА РОЩИ</h1>
<br>
Из-за леса, из-за рощи <br>
Вышла ротушка солдат. <br>
Эй, ли! Браво да люли, <br>
Вышла ротушка солдат. <br>
<br>
Перед ротой командир, да <br>
Хорошо маршировал. <br>
Эй ли! Браво да люли, <br>
Хорошо маршировал. <br>
<br>
Хорошо маршировал, да <br>
Останавливался. <br>
Эй ли! Браво да люли, <br>
Останавливался. <br>
<br>
Останавливался, да <br>
С Машей здравствовался. <br>
Эй ли! Браво да люли, <br>
С Машей здравствовался. <br>
<br>
— Здравствуй, любушка моя, да <br>
Дома ль матушка твоя? <br>
Эй ли! Браво да люли, <br>
Дома ль матушка твоя? <br>
<br>
— Дома вся наша семья, да <br>
Не хватает лишь тебя. <br>
Эй, ли! Браво да люли, <br>
Не хватает лишь тебя. <br>
