  Севастопольские песни <a href="kak8sent.php">"Как восьмого сентября"</a> и "Как четвертого числа" приписывались современниками Льву Толстому. Сам Лев Толстой в ответ на запрос М.Н. Милошевич, которая готовила к публикации воспоминания своего отца, участника обороны Севастополя, в двух письмах (от 19 февраля и от 18 мая 1906 г.) сообщил, что участвовал в сочинении &quot;Как восьмого сентября&quot; и сочинил &quot;Как четвертого числа&quot;, причем у обеих песен не было записанного авторского текста, и бытовали они устно. Тексты писем см.: <a href="../books/sreznevskij1925.pdf" target="_blank"><em>Срезневский В. К вопросу о принадлежности Л.Н. Толстому севастопольских песен</em></a><em> // Литературная мысль: [Альманах]. Вып. III. Л.: Мысль, 1925. С. 387-392.</em><br>
  <br> В собрании сочинений Льва Толстого песня печатается под заглавием: «Песня про 
  сражение на реке Черной 4 августа 1855 г.». Широкое распространение в списках 
  севастопольских песен <a href="kak8sent.php">«Как восьмого сентября…»</a> и 
  «Как четвертого числа…», вероятно, послужило серьезным препятствием в военной 
  карьере Толстого. В 1901 г. песня была переделана неизвестным лицом в связи 
  с разгоном студенческой демонстрации в Петербурге 4 марта 1901 г. - см. <a href="../starrev/grehtiaz.htm">&quot;Грех 
  тяжкий&quot; (Как четвертого числа...)</a>.<br>
