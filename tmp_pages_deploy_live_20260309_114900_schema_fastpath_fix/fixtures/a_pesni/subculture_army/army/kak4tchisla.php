	<h1>КАК 
  ЧЕТВЕРТОГО ЧИСЛА...</h1>
  
  Лев Толстой<br>
  <br>
  Как четвертого числа <br>
  Нас нелегкая несла <br>
  Горы занимать. <br>
  Барон Вревский енерал <br>
  К Горчакову приставал, <br>
  Когда под шефе: <br>
  «Князь, возьми ты эти горы, <br>
  Не входи со мной ты в споры, — <br>
  Право, донесу». <br>
  Собирались на советы <br>
  Всё большие эполеты, <br>
  Даже Плац-Бекок<br>
  Полицмейстер Плац-Бекок <br>
  Никак выдумать не мог, <br>
  Что ему сказать. <br>
  Долго думали, гадали, <br>
  Топографы всё писали <br>
  На большом листу. <br>
  Чисто писано в бумаге, <br>
  Да забыли про овраги, <br>
  Как по ним ходить. <br>
  Выезжали князья, графы, <br>
  А за ними топографы <br>
  На большой редут. <br>
  Князь сказал: «Ступай, Липранди!» <br>
  А Липранди: «Нет, ат<u>а</u>нде, <br>
  Я уж не пойду; <br>
  Туда умного не надо, <br>
  А пошли-ка ты Реада, <br>
  А я посмотрю». <br>
  А Реад - возьми да спросту <br>
  Поведи нас прямо к мосту: <br>
  «Ну-ка на уру!» <br>
  - Веймарн плакал, умолял, <br>
  Чтоб немножко обождал; <br>
  «Нет, уж пусть идут». <br>
  И «уру» мы прошумели, <br>
  Да резервы не поспели, <br>
  Кто-то переврал. <br>
  На Федюхины высоты <br>
  Нас всего пришло две роты, <br>
  А пошли полки. <br>
  Енерал-то Ушаков, <br>
  Тот уж вовсе не таков, <br>
  Всё чего-то ждал. <br>
  Долго ждал он, дожидался, <br>
  Пока с духом не собрался <br>
  Речку перейти. <br>
  А Белявцов енерал, <br>
  Тот все знамем потрясал, <br>
  Вовсе не к лицу. <br>
  Наше войско небольшое, <br>
  А французов ровно вдвое, <br>
  И сикурсу нет. <br>
  Ждали — выйдет с гарнизона <br>
  Нам на выручку колонна, <br>
  Подали сигнал, <br>
  А там Сакен енерал <br>
  Всё акафисты читал <br>
  Богородице. <br>
  И пришлось нам отступать, <br>
  &lt;…&gt; <br>
  Кто туда водил?!<br>
  А как первого числа<br>
  Ждали батюшку-царя<br>
  Мы у Фот-Сала.<br>
  И в усердном умиленьи<br>
  Ждали все мы награжденья, -<br>
  Не дал ничего.<br>
  <br>
