  <br>
«Полярная звезда», Вып. 3, Лондон, 1857, без подписи. «Собрание 
  стихотворений Пушкина, Рылеева, Лермонтова и других авторов» / Изд. Г. Гергарда», 
  Лейпциг, 1858 («Русская библиотека; т. 1); «Свободные русские песни», Берн, 
  1863; «Лютня: Собр. свободных русских песен и стихотворений» / Изд. Э.Л. Каспровича, 
  Лейпциг, 1869; «Лютня: Собр. свободных русских песен и стихотворений», 5-е изд. 
  Э.Л. Каспровича, Лейпциг, 1879.
		Впервые в легальной печати: «Русская старина». 1875, № 2, с вар. (по списку 
  М.И. Венюкова); № 3, с вар. (по списку С.П. Сысоева). Сводный текст песни, 
  составленный на основании семи списков, см. в изд.: Толстой Л.Н. Полн. собр. 
  соч. М., 1935. Т. 4. С. 307—308, там же ноты — напев песни. Об авторстве см. 
  примечание к стихотворению <a href="kak8sent.php">«Как восьмого сентября…»</a>. 
  Решающим аргументом в пользу авторства Л.Н. Толстого является его признание 
  в беседе с немецким ученым Р. Левенфельдом («Звезда». 1978, № 8. С. 118) и запись 
  Д.П. Маковицкого («Литературное наследство». 1979. Т. 90, кн. 1. С. 138). Шевченко, 
  встретившись с Л.Н. Толстым 8 апреля 1858 г., упоминает его в дневнике именно 
  как «автора солдатской севастопольской песни» (Шевченко Т. Г. Собр. соч. М., 
  1956. Т. 5. С. 223). <br>
 </span><br>
  <em>Как 
  четвертого числа.</em> Сражение 4 августа 1855 г. на речке Черной окончилось 
  для русских войск крайне неблагоприятно. <em>Барон Вревский-енерал К Горчакову 
  приставал. </em>Генерал П. А. Вревский (1808—1855) был командирован из Петербурга 
  в Севастополь для наблюдения за деятельностью М. Д. Горчакова (см. примеч. к 
  ст-нию «Как восьмого сентября…»). Под шефе — подвыпивший, навеселе (от фр. &eacute;chauff&eacute; 
  — разгоряченный). <em>Возьми ты эти горы </em>— позиции союзных войск на Гасфортовой 
  горе и Федюхиных высотах. <em>Собирались на советы.</em> Речь идет о военном 
  совете 28 июля 1855 г. с участием высших чинов армии (<em>большие эполеты</em>). <em>Плац-Бекок.</em> Генерал-полицмейстер А. П. Плац-бек-Кокум в совете, по-видимому, 
  не участвовал. <em>Большой редут </em>— новый редут вблизи Мекензиевых гор. <em>Липранди </em>— см. примеч. к ст-нию <a href="kak8sent.php">«Как восьмого 
  сентября…»</a>. <em>Ат<u>а</u>нде</em> — подожди (от фр. attendre — ждать). <em>А пошли-ка ты Реада.</em> Генерал Липранди командовал левым крылом, генерал 
  Н. А. Реад (1793—1855) — правым; последний повел войска в атаку, не дав ин завершить 
  построение, — это повлекло за собою ряд неудач. <em>Веймарн плакал, умолял. </em>Генерал П. В. Веймарн (убит в 1855), начальник штаба Реада, просил его 
  повременить с атакой. <em>Енерал-то Ушаков</em> и т. д. А. К. Ушаков (1803—1877) 
  медлил с атакой, ожидая резервов; <em>А Белявцов-енерал, Тот всё знамем потрясал.</em> Д. Н. Белевцов (1800—1883), прискакав к отступавшей дивизии, взял в руки знамя 
  и стал собирать вокруг себя бежавших солдат. <em>Сикурс</em> — помощь (искаж. 
  фр. — secours). <em>А там Сакен-енерал. Всё акафисты читал.</em> Д. Е. Остен-Сакен 
  (см. примеч. к ст-нию «Как восьмого сентября…») был известен своим ханжеством. 
  Яркая характеристика его набожности и пристрастия к акафистам (церковным хвалебным 
  песнопениям) дана Н. С. Лесковым в рассказе «Фигура». <em>Фот-Сала </em>(точнее 
  Фоти-Сала) — небольшая деревушка к востоку от Севастополя. <br>
  <br>
  <span class='i'>Вольная русская поэзия XVIII-XIX веков / Сост., подг. текста и примеч.  С.А. Рейсера. Л.: Сов. писатель, 1988 (Б-ка поэта. БС). С. 9-11, 546-547.</span><br>
