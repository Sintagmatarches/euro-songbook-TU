	<h1>КАК РАЗНЕССЯ СЛУХ ПО ПЕТРОПОЛЮ...</h1>

  Слова Антона Дельвига<br>
  <br>
  Как разнесся слух по Петрополю, <br>
  Слух прискорбнейший россиянину, <br>
  Что во матушку-Москву каменку <br>
  Взошли варвары иноземные. <br>
  То услышавши, отставной сержант <br>
  Подозвал к себе сына милого,<br>
  Отдавал ему свой булатный меч <br>
  И, обняв его, говорил тогда: <br>
  «Вот, любезный сын, сабля острая <br>
  Неприятелей разил коей я, <br>
  Бывал часто с ней во сражениях, <br>
  Умирать хотел за отечество <br>
  И за батюшку Царя белого. <br>
  Но тогда уже перестал служить, <br>
  Как при Требио калено ядро <br>
  Оторвало мне руку правую. <br>
  Вот еще тебе копье меткое, <br>
  С коим часто я в поле ратовал. <br>
  Оседлай, мой друг, коня доброго, <br>
  Поезжай разить силы вражески <br>
  Под знаменами Витгенштейна, <br>
  Вождя славного войска русского. <br>
  Не пускай врага разорить Русь <br>
  Иль пусти его через труп ты свой»<br>
  <br>
  7 сентября 1812<br>
  <br>
  <span class='i'>Требио - река в Италии, связанная с легендарным суворовским итальянским 
  походом. <br>
  <br>
  Ближайший лицейский друг Пушкина лифляндский барон Антон Дельвиг — один из создателей 
  жанра «русских песен», а первая из них была в тот исторический момент, когда 
  лицеисты-подростки 3 сентября наблюдали, как через Царское Сало проходят навстречу 
  врагу петербургские ополченцы. А через несколько дней, как гром средь ясного 
  неба, грянула весть о решении Кутузова сдать Москву. Сначала это был только 
  слух, казавшийся неправдоподобным. Но именно 7 сентября, когда Дельвиг выразил 
  свои чувства в стихах, Император получил депешу от московского генерал-губернатора 
  Федора Ростопчина и был потрясен не менее, чем тринадцатилетний лицеист. В 1815 
  году Пушкин напишет стихотворение «Наполеон на Эльбе», в котором ощутимы отзвуки 
  тех сентябрьских дней, а через много лет он пошлет Денису Давыдову свою «Историю 
  Пугачева» с надписью: «Тебе, певцу, тебе, герою! / Не удалось мне за тобою / 
  При громе пушечном, в огне / Скакать на бешеном коне...». То же самое желание 
  выражено и в песне Дельвига. <br>
  <br>
