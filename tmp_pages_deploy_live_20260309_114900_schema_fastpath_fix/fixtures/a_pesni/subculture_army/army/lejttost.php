	<h1>ЛЕЙТЕНАНТСКИЙ 
ТОСТ </h1>
<br>
Свой всему черёд, как говорится, <br>
Неспроста бокалов слышен звон: <br>
Чтобы ярче золоту светиться <br>
Лейтенантских новеньких погон! <br>
<br>
Чтоб отныне в жизни, как на марше, <br>
Каждый так равнение держал, <br>
Чтоб из лейтенанта вышел маршал, <br>
Ну, на крайний случай — генерал! <br>
<br>
Обретая чинные манеры, <br>
Выбор сделав раз и навсегда, <br>
Выпьем за удачу, офицеры, <br>
Кавалеры, дамы, господа! <br>
<br>
Впереди — сиянье звездопада <br>
И чинов высокий потолок... <br>
А за это точно выпить надо, <br>
Чтоб, как говорится, дай-то Бог! <br>
<br>
