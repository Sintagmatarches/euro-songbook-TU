	<h1>МЕЧТА ФРОНТОВОГО 
СОЛДАТА </h1>
<br>
Очень скоро вернусь на гражданку<br>
  И тогда непременно женюсь.<br>
  Соберу грандиозную пьянку<br>
  И конечно же в стельку напьюсь.<br>
<br>
Непременно женюсь на военной,<br>
  Буду крепко жену я любить,<br>
  Но чтоб жизнь не казалась ей медом<br>
  Будет в дзоте детей мне родить.<br>
<br>
И конечно родит она сына,<br>
  Настоящий он будет боец,<br>
  Попадет непременно в пехоту,<br>
  Где служил его родный отец.<br>
<br>
        А у дома, как будто на фронте,<br>
  Понарою я дзотов, траншей,<br>
  Буду утром гонять на зарядку<br>
  Всю семью от жены до детей.<br>
<br>
А чтоб милые наши детишки<br>
  В самоволку не смели гулять,<br>
  На кровати повешу я бирки,<br>
  Буду ночью их сам проверять.<br>
<br>
А в подвале построю губу я,<br>
  Злую тещу туда посажу,<br>
  Ну а сам, как отличник учебы,<br>
  В увольнение с другом схожу.<br>
  Ну а сам, как отличник учебы,<br>
  В увольнение с другом схожу.<br>
<br>
