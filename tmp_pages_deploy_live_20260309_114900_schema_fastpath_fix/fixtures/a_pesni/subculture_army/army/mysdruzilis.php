	<h1>МЫ 
СДРУЖИЛИСЬ С ЖИЗНЬЮ БОЕВОЮ </h1>
<br>
Мы сдружились с жизнью боевою, <br>
С жизнию солдата. <br>
Братцы, что ни шаг, то ближе к бою, <br>
Марш на супостата! <br>
<br>
Солдатушки, ребятушки, <br>
Куда вы путь держали? <br>
Враги звали, на пир приглашали. <br>
"Будем!" - мы сказали. <br>
<br>
Солдатушки, ребятушки, <br>
А есть у вас родимый? <br>
Есть родимый, отец наш любимый, <br>
Царь непобедимый. <br>
<br>
Солдатушки, ребятушки, <br>
А есть у вас родная? <br>
Есть родная, мать нам дорогая, <br>
Наша Русь святая. <br>
<br>
Солдатушки, ребятушки, <br>
А где же братья, сестры? <br>
С нами наши братья, наши сестры <br>
Штыки, сабли остры. <br>
<br>
Солдатушки, ребятушки, <br>
А где же ваша хата? <br>
Наша хата, народом богата - <br>
Лагерь супостата. <br>
<br>
Братцы, сильно нас приворожили <br>
Наши семьи, хаты, <br>
Даром мы на свете только жили, <br>
Ну, идем в солдаты! <br>
<br>
<span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006.</span><br>

