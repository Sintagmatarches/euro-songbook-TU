	<h1>НА 
  ГОРЫ УЛЕГСЯ ТУМАН </h1>

  <br>
  На горы улегся туман, <br>
  В ущелье свищет ветр порывный;<br>
  Готов ко штурму русский стан <br>
  И звука ждет трубы призывной. <br>
  <br>
  Зажглась вечерняя заря, <br>
  Загрохотали барабаны; <br>
  И вот молитву за Царя <br>
  Поют Кавказа ветераны. <br>
  <br>
  Среди заботы боевой, <br>
  Перед началом смертной сечи, <br>
  В беседе ратников живой <br>
  Кипят воинственные речи. <br>
  <br>
  Меж ними впереди сидел <br>
  Один из воинов унылый; <br>
  Он тихо песнь разлуки пел, <br>
  Ему взгрустнулося по милой. <br>
  <br>
  Он пел: о ветер, поспешай, <br>
  В родимый край несись стрелою <br>
  И милой весть о мне подай, <br>
  Скажи, что я храним судьбою... <br>
  <br>
  Звук трубный песню перервал; <br>
  Певец умолк, отряд собрался; <br>
  Уж к штурму подали сигнал, <br>
  И бой кровавый завязался... <br>
  <br>
  Наутро исполин-скала <br>
  С аулом - стал добычей боя; <br>
  Но уж дружина не нашла <br>
  В своих рядах певца-героя! <br>
  <span class='i'><br>
