        1. Как на главном Иркутском вокзале...</span><br>
         <br>
     Как на главном Иркутском вокзале<br>
Станционный смотритель смотрел,<br>
А на лавке под серой шинелью<br>
Молодой новобранец сидел.<br>
Перед ним, опустив на колени,<br>
Эта дева в слезах вся была,<br>
У ней вид такой грозно печальный,<br>
По плечам растрепалась коса.<br>
- Ты останься, останься, мой милый,<br>
На кого покидаешь меня?<br>
- Но я должен послушать начальство<br>
И покинуть навеки тебя!<br>
Паровозы гудки подавали,<br>
Пассажиры в вагоны спешат.<br>
Новобранец бросает красотку<br>
И скорее спешит в свой вагон.<br>
Не успел еще поезд отъехать,<br>
Не успел еще мост перейти,<br>
Как народ толпою собрался -<br>
Новобранца уж мертвым нашли.<br>
<br>
<span class='i'>Текст записан т. Элиасовым Л.Е. со слов т. Гальцовой А., с Б. Уро, Баргузинского аймака, БМАССР, 1937 г.<br>
<br>
Гуревич А.В., Элиасов Л.Е. Старый фольклор Прибайкалья. Т. 1. Улан-Удэ, 1939. С. 305. Раздел «Солдатские песни», №178.</span><br>
