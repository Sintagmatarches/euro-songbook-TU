	<h1>НА 
ВАРШАВСКОМ ВОКЗАЛЕ</h1>
	
<p><br>
  На Варшавском, на главном вокзале <br>
  Станционный смотритель прошел, <br>
  А на лавке под серой шинелью <br>
  Пригорюнясь, сидел офицер. <br>
  <br>
  Перед ним, опустясь на колени, <br>
  Стоит дева - младая краса. <br>
  В ее взоре - тоска и смущенье, <br>
  По плечам распустилась коса. <br>
  <br>
  - Ах, зачем я тебя полюбила, <br>
  Ах, зачем я любила тебя. <br>
  Из-за тебя я позорная стала, <br>
  Из-за тебя позабыла отца. <br>
  <br>
  - Верю-верю тебе, дорогая, <br>
  Что по-прежнему любишь меня, <br>
  Но я должен служить под начальством <br>
  И расстаться, Марьяна, с тобой... <br>
  <br>
  Увезут нас на поле сражений <br>
  И заставят окопы копать, <br>
  А потом раздадут по винтовке <br>
  И прикажут народ убивать. <br>
  <br>
  Ох, зачем нас берут во солдаты, <br>
  Угоняют на Дальний Восток. <br>
  Неужели мы в том виноваты, - <br>
  Грудью вышли на лишний вершок? <br>
  <br>
  Вот уж поезд к перрону подходит, <br>
  Из вагонов народ уж идет. <br>
  Офицер тут Марьяну покинул <br>
  И поспешно вскочил в эшелон. <br>
  <br>
  Не успел еще поезд отъехать, - <br>
  Ее тело несут на руках, <br>
  На перроне народ взволновался, <br>
  И начальник бежит сам не свой: <br>
  Его дочь от любви и измены <br>
  Под колеса легла головой. <br>
  <br>
  А в вагоне ему все не спится, <br>
  То и дело подходит к окну. <br>
  И на утро в том самом вагоне <br>
  Его труп офицерский нашли. <br>
  <br>
  ...Так скончалась их жизнь молодая <br>
  Из-за этой несчастной любви! <br>
  <br>
  <span class='i'>В нашу гавань заходили корабли: Песни. М.: Омега; Денис Альфа, 1995. С. 124-125.</span><br>
   <br>
  <br>
Эта трагикомическая песенка смешала разные эпохи - русско-японскую войну 1904-1905 гг. (&quot;угоняют на Дальний Восток&quot; и Первую мировую (отъезд на фронт с Варшавского вокзала). &quot;Убивать народ&quot; могли отправить с любой этой войны (соответственно, в ходе революции 1905-1907 гг. и 1917 г.). Варшавский вокзал 
  в Петербурге действительно был главным вокзалом империи, так как с него уходили поезда в европейские столицы.  По варшавской линии ходили лучшие вагоны, по ней же курировал императорский поезд. Строфа:<br>
  <br>
  <em>Ох, зачем нас берут во солдаты, <br>
  Угоняют на Дальний Восток. <br>
  Неужели мы в том виноваты, - <br>
  Грудью вышли на лишний вершок? </em><br>
  <br>
  - это начало популярной солдатской  песни времен русско-японской войны 
