     <span class='bold'>2. Как на Харьковском южном вокзале...<br></span>
     <br>
Как на Харьковском южном вокзале<br>
 Станционный гудок прогудел.<br>
А на лавке под серой шинелью<br>
 Призадумавшись, сидел офицер.<br>
<br>
Перед ним, опустясь на колени,<br>
Стоит дева, как роза-краса.<br>
Её вид, такой грустный, унылый,<br>
По плечам расстилалась коса.<br><br>
- Милый, милый, куда уезжаешь?<br>
 Ты, наверно, не любишь меня.<br>
Ты, наверно, другую полюбишь?<br>
Ах, какая несчастная я.<br>
<br>
Ты останься, мой милый, со мною,<br>
Я прошу, не губи ты меня.<br>
Лучше лягу живой я в могилу,<br>
Но другого любить я не могла.<br>
<br>
Загудели гудки паровозов,<br>
Станционный бежит, сам не свой.<br>
Его дочь от любви, от измены<br>
Под курьерский легла головой.<br>
<br>
Не успел ещё поезд отъехать,<br>
Не успел он моста перейти,<br>
Мужики по перрону толпою<br>
Неостывшее тело несли.<br>
