	<h1>СОЛДАТСКАЯ 
ПЕСНЯ </h1>

Слова Николая Ильина<br>
<br>Ночь темна была и не месячна,<br>
  Рать скучна была и не радостна;<br>
  Все солдатушки призадумались,<br>
  Призадумавшись, горько всплакали.<br>
  Велико чудо совершилося:<br>
  У солдат слезы градом сыпались.<br>
  Не люта змея кровожадная<br>
  Грудь сосала их богатырскую,<br>
  Что тоска грызла ретивы сердца,<br>
  Ретивы сердца молодецкие,<br>
  Не отцов родных оплакивали<br>
  И не жен младых и не детушек, -<br>
  Как оплакивали родимую,<br>
  Мать родимую, мать кормилицу,<br>
  Златоглавую Москву милую,<br>
  Разоренную Бонапартием.<br>
  Тут как вдруг они встрепенулися,<br>
  Словно вихрь – орлы взор скинули<br>
  Во все стороны и воскликнули<br>
  Все в один голос, как в злату трубу:<br>
  «Молодцы братцы, удалы друзья,<br>
  Неизменные чада русские!<br>
  Что дадим, братцы, клятву кровную,<br>
  Клятву кровную, задушевную,<br>
  Чтоб не взвидеть нам и домов своих,<br>
  Ни отцов родных, ни младых нам жен,<br>
  Малых детушек, роду-племени,<br>
  Ни самой души красной девицы,<br>
  Не побив силы Бонапартовой,<br>
  Не отмстив врагу за родну Москву!<br>
  Отсечем ему мы возвратный путь,<br>
  И мы примемся по-старинному,<br>
  По-старинному, по-суворовски;<br>
  Закричим «ура!» и пойдем вперед;<br>
  На штыках пройдем силы вражие;<br>
  Перебьем мы их, переколем всех;<br>
  Кто пяток убьет, кто десяточек,<br>
  А лютой боец по пятнадцати.<br>
  Не дадим, друзья, люта промаху;<br>
  Постараемся все, ребятушки,<br>
  Чтобы сам злодей на штыке погиб,<br>
  Чтоб вся рать его здесь костьми легла,<br>
  Ни одна б душа иноверная<br>
  Не пришла назад в свою сторону».<br>
