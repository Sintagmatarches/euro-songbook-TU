  Музыка Петра Булахова<br>
  Слова Ивана Лажечникова<br>
  <br>
  Отпусти меня, родная, <br>
  Куда просится душа, <br>
  Не обрезывай ей крылья: <br>
  Жизнь ей волей хороша, <br>
  Жизнь ей волей хороша. <br>
  <br>
  Соколы, когда слетались, <br>
  Как на пир, на смертный бой, <br>
  Я ж один в железной клетке <br>
  Просижу час роковой, <br>
  Просижу час роковой. <br>
  <br>
  Посмотри-ка, как я франтом <br>
  Возвращусь с военных сечь: <br>
  При бедре "За храбрость" меч, <br>
  На груди "Владимир" с бантом. <br>
  <br>
  И тогда в собраньях скажут: <br>
  "Молод так, а кавалер, <br>
  Видно, храбрый офицер..." <br>
  И на мать мою укажут, <br>
  И на мать мою укажут. <br>
  <br>
  &lt;1850-е гг.&gt; <br>
  <br>
  <span class='i'>Из оперетты "Новобранец". Музыка Петра П. Булахова (1850-е гг.), Ю.Д. Энгеля 
  (1909). Публикуется по нотному изданию. <br>
  <br>
  Вместе с Василием Жуковским и Петром Вяземским среди добровольцев Московского 
  ополчения был и сбежавший из дома двадцатидвухлетний Иван Лажечников, участник 
  в сражениях под Тарутином, Малоярославцем, Красным, заграничного похода. Позднее 
  он издаст свои "Походные записки русского офицера", которым, как и "Письмам 
  русского офицера" Федора Глинки, суждено будет стать классикой русской военной 
  мемуаристики. Прославится же он, как исторический писатель, автор популярнейших 
  романов "Последний Новик, или Завоевание Лифляндии в царствование Петра Великого" 
  (1831), "Ледяной дом" (1834), "Басурман" (1838), навсегда закрепивших за ним 
  славу "русского Вальтера Скотта". Позднее, в 1840-1850-е годы, выйдут и другие 
  его произведения, в том числе стихотворные драмы, но в исторической трилогии 
  поэзия тоже присутствовала. Иван Лажечников, как и многие другие прозаики того 
  времени, вводил в свои произведения "русские песни", которые "озвучивали" композиторы. 
  Наибольшей популярностью пользовался романс "Сладко пел душа-соловушко" на музыку 
  А.А. Алябьева (1834) и А.Е. Варламова (1838), а также две солдатские песни: 
  "Новобранец" и <a href="soldatik.php">"Солдатик"</a>, музыку к которым в 1850-е 
  годы написал Петр П. Булахов. <br>
