	<h1>ПЕСНЯ 
  НА СБОРЕ ВОЙСК </h1>
  Слова Николая Веревкина<br>
  На голос: Нам не то отцы заповедали <br>
  <br>
  Не горы, горы полуночной, <br>
  <div class='refren'>Ветер по полю бежит; </div>
  Не летят, летят соколики <br>
 <div class='refren'>Из-под кустиков ракит; </div>
  Нет! сойдясь близ Ковны-города, <br>
  <div class='refren'>Как румяная заря </div>
  Ярко светятся солдатушки <br>
 <div class='refren'>Православного царя. </div>
  Что вы, что вы, добры молодцы? <br>
 <div class='refren'>Собрались опять в поход? </div>
  Уж не слышно ль снова за морем <br>
<div class='refren'>Мятежей и непогод? </div>
  Уж не снова ль штуку выкинул <br>
 <div class='refren'>Окаянный басурман? </div>
  Уж не вновь ли взволновалися <br>
<div class='refren'>Аль земля, аль океан? </div>
  Нет! не с тем сюда мы собраны, <br>
  <div class='refren'>Чтоб идти на басурман. </div>
  Нет! покорны нам, ребятушки, <br>
 <div class='refren'>И земля и океан! </div>
  Мы за тем под Ковной собраны, <br>
  <div class='refren'>Чтобы встретить молодца, </div>
  Государя Императора, <br>
<div class='refren'>Красно-солнышко, отца! </div>
  Чтоб, сказав ему, державному, <br>
 <div class='refren'>Как он пламенно любим, - </div>
  Доказать его величеству, <br>
  <div class='refren'>>Отличившись перед ним, - </div>
  Что наш корпус, в битвах опытный, <br>
 <div class='refren'>Верный долгу своему, </div>
  Первый будучи по нумеру <br>
		<div class='refren'>Будет первым по всему!</div> <span class='i'><br>
