	<h1>ПИСЬМО СОЛДАТА 
</h1>
<br>
Привет из далекого края,<br>
  Где одежду бесплатно дают,<br>
  Где за тридцать секунд одевают<br>
  И в столовую строем ведут.<br>
<br>Ты идешь на учебу спокойно,<br>
  В это время тревога у нас,<br>
  А когда спишь в постели спокойно,<br>
  Я стою на посту в этот час.<br>
<br>А когда молодые ребята<br>
  Вечерами целуют девчат,<br>
  Я стою, о тебе я мечтаю,<br>
  Прижимая к груди автомат.<br>
<br>Ты танцуешь под музыку танго,<br>
  В это время я бегаю кросс,<br>
  Косы черные ты заплетаешь,<br>
  Я в окошко смотрю без волос.<br>
<br>Ты ругаешься сидя с начальством,<br>
  Я навытяжку смирно стою,<br>
  Ты уходишь оттуда спокойно,<br>
  Я иду от него строевым.<br>
<br>Тебя вежливо будит динамик,<br>
  Мне дневальный на ухо кричит,<br>
  А пока ты работаешь ложкою,<br>
  Я раз пять сто дорог оббегу.<br>
<br>Я кончаю писать, до свиданья.<br>
  «Построенье!» - кричит старшина.<br>
  Выпей рюмку, меня дожидая,<br>
  До свиданья, целую тебя.<br>
<br>
<span class='i'>В нашу гавань заходили 
  корабли. Вып. 2. М.: Стрекоза, 2000.</span><br>
