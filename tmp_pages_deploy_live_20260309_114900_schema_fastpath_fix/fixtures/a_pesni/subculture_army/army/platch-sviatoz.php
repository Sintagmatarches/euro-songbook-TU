	<h1>ТЫ ПРОЩАЙ-КО, МОЕ РОЖОНОЕ МИЛОЕ ДИТЯТКО...<br>
	Плач святозерской крестьянки по рекруту</h1>

 Ты прощай-ко, мое рожоное милое дитятко, <br>
  Моя любимая удалая наживная головушка! <br>
  Как обневолили тебя не в порушку, да не во времечко, <br>
  Моего полетного, ясного сокола, <br>
  Из-под моего правого крылышка. <br>
  Ты пойдешь, моя любимая удалая, наживная головушка, <br>
  На чужую-дальную сторонушку, <br>
  Что ль по дальней ш<strong>и</strong>рокой дороженьке; <br>
  Шириной дорожка тридцать сажень, <br>
  Длиной дорожка — конца краю нет! <br>
  Ты вспомни-ко свою любимую домашнюю крестьянскую жирушку, <br>
  И ты вспомни-ко меня, престарелую многопобедную головушку!<br>
  И как придут-то честные годовые воскресные владычные празднички; <br>
  И как наступят любимые веселые крестьянские гуляньица, <br>
  Хороводы да игрища; <br>
  Как пойдут-то молодые, удалые добрые м<strong>о</strong>лодцы, <br>
  Что ль твои ли сотоварищи; <br>
  Уж как я, многопобедная головушка, <br>
  Я, горюша горегорькая,<br>
  Как я сяду-то под свое любимое косящато окошечко, <br>
  Я покукую кокушицей, <br>
  И как вспомню про тебя мое рожоное дитятко, <br>
  Моя наживная, удалая головушка, <br>
  Мое солнышко закатное, <br>
  Моя звездочка ненаглядная!<br>
  Ты не дай-ко, боже-господи, <br>
  Нам живого расставаньица <br>
  С моим милым-любимым дитятком! <br>
  И как есть-то мне тошнешёнько, <br>
  И как есть-то мне больнёшенько, <br>
  Моему сердцу ретивому! <br>
  И как придет-то теперь времечко, <br>
  Что ль весна придет да красная, <br>
  Придет летушко ли теплое, <br>
  Как-то стану я многопобедная головушка,<br>
  Со своим со малыим рожоныим дитятком?<br>
  Как я стану обработывать <br>
  Свою землю хлебопашеством, <br>
  Свои поженки прокосные, <br>
  И луга-нивы продольные, <br>
  Все поречья и все з<strong>а</strong>речья, <br>
  Все мелки кусты-кустарнички, <br>
  Все лазуревы алые цветики? <br>
  И ты, любимая удалая головушка, <br>
  Ты уведомь меня бедную, <br>
  Мать, горюшу горегорькую, <br>
  Писемцом либо грамоткой, <br>
  Или ласковым словечушком! <br>
  И ты не помни, мое милое, <br>
  Всех житейских моих грубостей; <br>
  И ты вспомни-ко, рожоное <br>
  Мои ласк<strong>о</strong>вые словечушки! <br>
  И как я, многопобедная, <br>
  Я, горюша горегорькая, <br>
  Попрошу царя небесного <br>
  И земного вседержителя, <br>
  Чтоб открыл он тебе ученьице <br>
  И всю службу государеву!<br>
