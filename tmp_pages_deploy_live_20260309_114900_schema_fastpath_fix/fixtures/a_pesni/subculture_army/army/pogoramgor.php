	<h1>ПО ГОРАМ-ГОРАМ...</h1>
	<br>
	По горам-горам 
  <br>
  По высоким <br>
  Млад сизой орел <br>
  Высоко летал, <br>
  Высоко летал, <br>
  Жалобно кричал. <br>
  Во строю солдат <br>
  Тяжело вздыхал: <br>
  «Мне не жаль, не жаль <br>
  Самого себя, <br>
  Только жалко мне <br>
  Зелена сада: <br>
  Во зеленом саду <br>
  Есть три деревца; <br>
  Перво деревцо – <br>
  Кипарисово, <br>
  Друго деревцо — <br>
  Сладка яблонка, <br>
  Третье деревцо –<br>
  Зелена груша. <br>
  Кипарис-дерево — <br>
  Родной батюшка; <br>
  Сладка яблонка – <br>
  Родна матушка, <br>
  Зелена груша –<br>
  Молода жена».<br><br>
  <span class='i'>Русские народные песни 
  / Сост. и вводн. тексты В.В. Варгановой.  М.: Правда, 1988. Раздел «Песни 
  солдат и о солдатах. Рекрутские причитания».</span><br>

