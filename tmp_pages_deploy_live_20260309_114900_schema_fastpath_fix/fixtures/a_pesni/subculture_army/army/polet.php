	<h1>ПОЛЕТ 
</h1>

Крутится, вертится шар голубой – <br>
Наш самолет отправляется в бой. <br>
«Тра-та-та-та», — бортмеханик сказал, <br>
«Тра-та-та-та», — командир отвечал.<br>
<br>
Штурман заснул средь бутылок пустых, <br>
Мы в облаках заблудились густых. <br>
Тра-та-та-та, командир очень злой, <br>
Тра-та-та-та, полетели домой.<br>
<br>
Вот бортмеханик в кабину вбежал: <br>
«Шухер, ребята, тра-та нам настал». <br>
Чья-то ракета за нами летит. <br>
«Тра-та-та-та», — командир говорит. <br>
<br>
Прямо с небес шарахнулись во мрак, <br>
Кто же подбил нас — свой или враг? <br>
Наша ракета и наш самолет, <br>
Как оказалось — знакомый пилот. <br>
<br>
«Привет, Васек! Давай, ребята». <br>
На белый свет экипаж вылезал. <br>
Каждый из них «тра-та-та-та» сказал. <br>
Лишь командир ничего не сказал. <br>
<br>
В легкие воздух он набирал: <br>
«Тра-та-та-та-та-та, тра-та-та-та, <br>
Ты не механик, ты — тра-та-та-та. <br>
Тра-та-та. Тра-та-та. Тра вашу мать. <br>
Больше не буду я с вами летать!» <br>
