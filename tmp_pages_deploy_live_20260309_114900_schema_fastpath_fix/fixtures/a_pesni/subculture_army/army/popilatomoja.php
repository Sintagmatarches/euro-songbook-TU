	<h1>ПОПИЛА-ТО МОЯ БУЙНАЯ ГОЛОВУШКА, ПОПИЛА, ПОГУЛЯЛА...</h1>
<br>
	Попила-то моя буйная головушка, попила, погуляла <br>
  Что за батюшкиной, да за матушкиной большой головою,<br>
  Что за братцевой, за невесткиной тяжелой работой, <br>
  Как не бел гребень головушку чешет - чешет пора-время,<br>
  Чешет пора-времечко, гульба-воля, удаль молодецкая, вольное раздолье, <br>
  Вдруг 
  почуяло мое сердечушко невзгоду. <br>
  Та невзгода ли, невзгода — частые наборы. <br>
  Что хотят-то меня, молодца, всем миром поймать, <br>
  Белы рученьки связать, резвы ноженьки сковать; <br>
  Не поймавши добра молодца, как же в рекруты отдать? <br>
  Не сыскавши красной девушки, кого ж замуж обвенчать?<br>
  Как поймали добра молодца у прилуки красной девицы,<br>
  Белы рученьки связали, резвы ноженьки сковали; <br>
  Посадили меня, молодца, на мирски подводы, <br>
  Повезли-то меня, молодца, во город Саратов. <br>
  Во городе во Саратове бреют кудри не жалеючи. <br>
  Приводили молодца в приемную, <br>
  Становили разудалого под меру казенную <br>
  И обрили молодцу кудри русые. <br>
  Не видать моим кудерюшкам уже прежней холюшки; <br>
  Уж не будет добру молодцу гульбы-волюшки! <br>
  Соберите, братцы милые, мои кудри русые, <br>
  Завяжите их в миткалевый платок, <br>
  Отошлите к матушке в терем высок! <br>
