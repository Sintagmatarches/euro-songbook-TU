  Восстать сынам против врага!<br>
  Пора за родину святую<br>
  Идти на бой, на бой... Ура!<br>
  <br>
  Бог нам на помощь, Бог над нами!<br>
  Русский народ, на страже стой!<br>
  Встань за отчизну, встань за веру.<br>
  За православный Крест святой.<br>
  <br>
  Пора! Под знамя боевое<br>
  Зовет нас родина, пора!<br>
  Вперед! Пусть вспомнит враг былое,<br>
  Пусть знает русский бой, Ура!<br>
  <br>
  Край наш родимый, Русь святая,<br>
  Нас на борьбу благословляй!<br>
  Мы за свободу, мы за славу,-<br>
  За неделимый русский край.<br>
