	<h1>ПОСЛЕ 
БИТВЫ ЗА ДУНАЕМ…</h1>
<br>
После битвы за Дунаем, <br>
В басурманской стороне, <br>
Умирая после боя, <br>
Воин слово молвил мне: <br>
"Отнеси, брат, в край родимый, <br>
После моих похорон, <br>
Челобитьице родимой <br>
И родимому поклон, <br>
А жене своя ей воля <br>
Стать другой раз под венец; <br>
Видно, брат, моя не доля, <br>
И жене не муж мертвец. <br>
Весть неси вдове такую, <br>
Что женат я на другой, <br>
Что с другою век свекую <br>
Я под крышей вековой: <br>
Что за нею на погосте <br>
Взял я каменный накат, <br>
Пуля сватала, а гости <br>
Были пушки да булат". <br>
<span class='i'><br>
Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006.</span><br>
