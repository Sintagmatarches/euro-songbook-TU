	<h1>ПРИМИ, 
  О РОДИНА СВЯТАЯ! </h1>

  Слова Григория Малышева<br>
  <br>
  Прими, о родина святая! <br>
  Твоих защитников, сынов, <br>
  С победой славной от Дуная <br>
  Спешим к тебе под мирный кров. <br>
  <br>
  Там слаще воздух, день светлее, <br>
  Там в мире счастие цветет, <br>
  Там чувство радости живее, <br>
  Где кров родной и друг нас ждет. <br>
  <br>
  Спешим из стран, где враг грозился <br>
  Внести войну в родной наш край, <br>
  Но русский меч блеснул, сразился... <br>
  И в Варне с нами - Николай! <br>
  <br>
  Спешим... день за день скоро мчатся, <br>
  И вдруг, о радость!.. край родной!.. <br>
  Сердца родных соединятся, <br>
  Забыв разлуку меж собой. <br>
  <br>
  Но в день возврата, день счастливый, <br>
  Не всем равно восторг делить: <br>
  Вдова и мать с душой унылой <br>
  О павших будут слезы лить! <br>
  <br>
  Велик Бог милостью благою! <br>
  Несчастным, сирым - Он Отец; <br>
  А павшему в бою герою <br>
  С небес - бессмертия венец. <br>
  <br>
  1828 <br>
  <br>
  <span class='i'>Николай I принимал участие в военных действиях в Русско-турецкой войне с 
  мая по октябрь 1828 года. Занятие турецкой крепости Баязет и капитуляции Варны 
  решили исход войны в пользу России. Григорий Малышев был в это время на фронте. 
  Его песни об этой войне и польской кампании отражали реальную, а не виртуальную 
  действительность того времени. <br>
  <br>
Антология военной песни / Сост. и автор предисл. В. Калугин. 
  М.: Эксмо, 2006.</span><br>
  <br>
  <br>
  <strong>Малышев Григорий Григорьевич (1802-1868)</strong> - солдат, поэт, военный 
  музыкант, прошел две войны, включая русско-турецкую 1828-1829, получив чин унтер-офицера. 
  В 1835 г. за исполнение своей песни &quot;Русский царь набрал дружину...&quot; 
  перед монархами России, Пруссии и Австрии, переведен в придворную певческую 
  капеллу.<br>
