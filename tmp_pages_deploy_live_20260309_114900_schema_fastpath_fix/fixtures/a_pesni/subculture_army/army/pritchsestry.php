	<h1>СОКОЛОЧЕК ДА МИЛОЙ БРАТЕЛКО...<br>
	Причеть сестры по брату, идущему в солдаты</h1>

Соколочек да милой брателко, <br>
Ты куды да наряжаешься, <br>
Ты куды да сподобляешься, <br>
Во какую да путь-дороженьку? <br>
Не в любую да подороженьку, <br>
Ко судьям да немилостливым, <br>
Как к сердцам да нежалостливым, <br>
Как заведут, да сокол брателко, <br>
Во присусьё да великоё, <br>
Как поставят, да сокол брателко, <br>
Тебя под мерушку казенную, <br>
Станут брить, да сокол брателко, <br>
Всё твои да всё русы кудри, — <br>
Повалятся да русы кудри <br>
Со буйные головушки, <br>
Как у тебя, да сокол брателко, <br>
Твои да всё русы кудри! <br>
Как на кажной волосиночке <br>
По горячей по слезиночке! <br>
Как прилетала да птичка-пташечка <br>
Ко косявчету окошечку. <br>
Как будила да птичка-пташечка <br>
Соколочка да мила брателка, <br>
Воспевала да птичка-пташечка <br>
Слезяно да очень жалобно, <br>
Как залетала да птичка-пташица <br>
Как во светлую да светлицу, <br>
Как садилась да птица-пташица <br>
На зголовьицо высокое, <br>
Как будила да птичка-пташица <br>
Соколочка да мила брателка: <br>
Полно спать да высыпатися, <br>
Пора ставать да пробуждаться, <br>
Всё во путь да во дороженьку <br>
Как тебе да наряжаться, <br>
Всё во город да во Кириллово, <br>
Ко судьям да немилостливым, <br>
Ко сердцам да нежалостливым! <br>
