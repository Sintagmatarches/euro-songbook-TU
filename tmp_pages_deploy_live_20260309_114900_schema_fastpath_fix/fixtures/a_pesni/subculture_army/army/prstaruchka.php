	<h1>ПРОЩАЙ, СТАРУШКА МОЯ РОДНАЯ...</h1>
  <br>
Прощай, старушка моя родная,<br>
	  Прощайте, милые друзья.<br>
	  Благослови-ка, мать родная,<br>
	  Быть может еду насмерть я.<br>
	  Может еду, не приеду,<br>
	  Назад не ворочусь;<br>
	  Быть может меткая винтовка<br>
	  Из-под куста сразить меня.<br>
	  Сразить францус меня проклятый,<br>
	  Слехка мой цереп раздробит.<br>
	  Прольетца кровь моя горячая,<br>
	  Прольетца самую реку.<br>
	  Никто по крови не поплачеть,<br>
	  Никто слезинку не прольеть.<br>
	  Прольеть сестра моя родная,<br>
	  Прольеть горячую слезу.<br>
	  Куплю сестре своей платоцек,<br>
	  Пошлю подарок дорогой.<br>
	  Носи, сестра, мой подарок<br>
	  Носи и брата поминай.<br><br>
	  <span class='i'>Текст записан т. Дмитриевым К.А. со слов т. Громовой Е.И., 50 л. и Солодухиной П.Т., 21 г., чл. колхоза им. Молотова, с. Б. Куналей, Тарбагатайского аймака, БМАССР, 1936 г.<br><br>
	  Гуревич А.В., Элиасов Л.Е. Старый фольклор Прибайкалья. Т. 1. Улан-Удэ, 1939. С. 303-304. Раздел «Солдатские песни», №175.<br>
		</span><br>
