	<h1>ПЕСНЯ 
СЕЛИМА</h1>

Слова Михаила Лермонтова<br>
<br>
<div class='refren'>Месяц плывет</div>
И тих и спокоен; <br>
А юноша-воин <br>
На битву идет. <br>
Ружье заряжает джигит, <br>
И дева ему говорит: <br>
<br>
<div class='refren'>"Мой милый, смелее</div>
Вверяйся ты року, <br>
Молися востоку, <br>
Будь верен Пророку, <br>
Любви будь вернее! <br>
<br>
<div class='refren'>Всегда награжден,</div>
Кто любит до гроба, <br>
Ни зависть, ни злоба <br>
Ему не закон; <br>
Пускай его смерть и погубит; <br>
Один не погибнет, кто любит! <br>
<br>
<div class='refren'>Любви изменивший</div>
Изменой кровавой, <br>
Врага не сразивши, <br>
Погибнет без славы; <br>
Дожди его ран не обмоют, <br>
И звери костей не зароют!" <br>
<br>
<div class='refren'>Месяц плывет </div>
И тих и спокоен; <br>
А юноша-воин <br>
На битву идет! <br>
