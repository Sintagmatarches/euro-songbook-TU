	<h1>ПЕСНЯ 
«СТАРИКОВ» </h1>
<br>
Если ты головой поник, <br>
Я скажу: не грусти, «старик»!<br>
Если снова в наряд идёшь, <br>
Я скажу: ну так что ж! <br>
<br>
Всё равно нам пора домой! <br>
Будет кто-то ходить другой. <br>
Слушать ротного на плацу <br>
Старику не к лицу. <br>
<br>
Вот и март, и апрель прошли, <br>
Сапоги мы себе нашли, <br>
Я ремень с молодого снял – <br>
Он стонал, но отдал. <br>
<br>
Двадцать рваных с трудом скопил — <br>
Чемодан я себе купил. <br>
Я два года приказа ждал! <br>
Наконец этот день настал! <br>
<br>
Не сердитесь, друзья, на нас – <br>
День бывает такой лишь раз! <br>
Побегут поезда на юг, <br>
Где увидим подруг. <br>
