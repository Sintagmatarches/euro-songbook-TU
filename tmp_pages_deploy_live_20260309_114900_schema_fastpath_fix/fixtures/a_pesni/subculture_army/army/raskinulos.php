	<h1>РАСКИНУЛОСЬ 
  МОРЕ ШИРОКО<br>
  </h1><br>
  Раскинулось море широко, <br>
  И волны бушуют вдали. <br>
  Товарищ, мы едем далеко, <br>
  Подальше от нашей земли. <br>
  <br>
  «Товарищ, я вахты не в силах стоять, — <br>
  Сказал кочегар кочегару, — <br>
  Огни в моей топке совсем не горят, <br>
  В котлах не сдержать больше пару. <br>
  <br>
  Поди доложи ты, что я заболел, <br>
  И вахты, не кончив, бросаю, <br>
  Весь потом истек, от жары занемог, <br>
  Работать нет сил, помираю!»<br>
  <br>
  Товарищ ушел, он лопату схватил, <br>
  Собравши последние силы, <br>
  Движеньем привычным он печь отворил, <br>
  И пламя его опалило. <br>
  <br>
  Закончив бросать, он напился воды,<br>
  Воды опресненной, нечистой.<br>
  С лица его падал пот, сажи следы, <br>
  Заслышал он речь машиниста: <br>
  <br>
  «Ты вахты до срока не смеешь бросать, <br>
  Механик тобой недоволен.<br>
  Ты к доктору можешь пойти и сказать, <br>
  Лекарства он даст, если болен!» <br>
  <br>
  На палубу вышел, сознанья уж нет. <br>
  В глазах у него помутилось... <br>
  На миг увидал ослепительный свет... <br>
  Упал... Сердце больше не билось. <br>
  <br>
  Наутро проститься с покойным пришли <br>
  Матросы, друзья кочегара, <br>
  Подарок последний ему принесли -<br>
  Колосник тяжелый и ржавый.<br>
  <br>
  К ногам привязали ему колосник, <br>
  В суровую ткань обернули.<br>
  Пришел корабельный священник-старик,<br>
  И слезы у многих сверкнули.<br>
  <br>
  Напрасно старушка ждет сына домой, — <br>
  Ей скажут, она зарыдает... <br>
  А волны бегут от винта за кормой,<br>
  И след их вдали пропадает.<br>
  <br>
<span class='i'>Расшифровка фонограммы Андрея Макаревича, альбом «Песни, которые 
  я люблю», Sintez Records, 1996, подпись: А. Гурилев - Г. Зубарев.</span><br>
<br>
  <br>
  Другое название песни - &quot;Кочегар&quot;.<br>
  <br>
  Создана не позднее грани XIX-XX вв. на основе популярного в годы Крымской войны 
  1853-56 гг. романса <a href="neslychno.php">&quot;После битвы&quot; </a> (&quot;Не 
  слышно на палубе песен...&quot;, слова Николая Щербины, 1843, музыка Александра 
  Гурилева, 1852, там же см. первоначальную мелодию). Романс был очень популярен 
  на флоте, изменялся во флотской среде, и к началу 1900-х гг. итогом этих изменений стала песня &quot;Раскинулось море широко&quot;. Основной ее сюжет - новый, 
  не связанный с первоначальным романсом. Изысканно-грустная мелодия Гурилева в песне стала проще. Вводная строфа, а иногда несколько вводных строф сохраняют поэтический размер и отчасти текст романса, а сама история кочегара (от слов &quot;Товарищ, я вахты не в силах стоять...&quot; ) сложена другим размером - добавлены безударный и ударный слоги в нечетных строках и безударный в четных. <br>
  <br>
  По наиболее распространенной версии, автор текста песни - моряк, поэт-любитель 
  Георгий Зубарев, погибший вскоре в Цусимском сражении. Каким в точности 
  был авторский текст Зубарева, неизвестно. У песни встречается свыше 20 куплетов, 
  бытуют обычно 12-15.<br>
  <br>
  Песня входила в репертуар Надежды Плевицкой, в музыкальную композицию Леонида Утесова 
  &quot;Два корабля&quot; (1937), что способствовало ее популяризации. Утесов вспоминал, что знал и пел эту песню со своего одесского детства, что 
  она была особенно популярной накануне революции 1905 г., была длинной, а для 
  &quot;Двух кораблей&quot; он взял, по его мнению, не больше одной пятой части 
  текста (Утесов Л. С песней по жизни. М.: Искусство, 1961. С. 169).<br>
  <br>
  &quot;Раскинулось море широко&quot; послужила основой для множества дальнейших переделок. Она была одной из 
  наиболее часто переделываемых песен во времена Второй мировой войны: <a href="../ww2/folk/rask-sev.htm">гимн 
  севастопольцев</a>, <a href="../ww2/folk/partizanskaja.htm">&quot;Партизанская&quot;</a> 
  (&quot;Раскинулась роща широко...&quot;), <a href="../ww2/folk/raskinrelsy.htm">&quot;Раскинулись 
  рельсы широко&quot;</a>, <a href="../ww2/folk/raskinmore-ww2.htm">&quot;Раскинулось 
  море широко (О Второй Мировой)&quot;</a>, <a href="../ww2/folk/javstretsnim.htm">&quot;Я 
  встретился с ним под Одессой родной&quot;</a> и т. д.<br>
  <br>
  Подробнее см.: Михаил Лезинский. <a href="../popular20/a-raskin.php">И волны бушуют вдали 
  - история одной песни</a> (2007).<br>
