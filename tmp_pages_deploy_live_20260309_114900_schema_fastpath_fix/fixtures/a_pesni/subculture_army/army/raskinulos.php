  1.  Раскинулось море широко...</span><br>
<br>
Раскинулось море широко, <br>
И волны бушуют вдали. <br>
Товарищ, мы едем далеко, <br>
Подальше от нашей земли.<br>
<br>
Не слышно на палубе песен, <br>
И Красное море волною шумит, <br>
А берег суровый и тесен, — <br>
Как вспомнишь, так сердце болит.<br>
<br>
На баке уж восемь пробило –<br>
Товарища надо сменить.<br>
По трапу едва он спустился,<br>
Механик кричит: «Шевелись!»<br>
<br>
«Товарищ, я вахты не в силах стоять, — <br>
Сказал кочегар кочегару, — <br>
Огни в моих топках совсем прогорят;<br>
В котлах не сдержать мне уж пару.<br>
<br>
Пойди заяви, что я заболел, <br>
И вахту, не кончив, бросаю, <br>
Весь потом истек, от жары изнемог, <br>
Работать нет сил - умираю».<br>
<br>
Товарищ ушел... Лопату схватил, <br>
Собравши последние силы, <br>
Дверь топки привычным толчком отворил, <br>
И пламя его озарило:<br>
<br>
Лицо его, плечи, открытую грудь<br>
И пот, с них струившийся градом, -<br>
О, если бы мог кто туда заглянуть,<br>
Назвал кочегарку бы адом!<br>
<br>
Котлы паровые зловеще шумят,<br>
От силы паров содрогаясь,<br>
Как тысячи змей пары же шипят,<br>
Из труб кое-где пробиваясь.<br>
<br>
А он, извиваясь пред жарким огнем,<br>
Лопатой бросал ловко уголь;<br>
Внизу было мрачно: луч солнца и днем<br>
Не может проникнуть в тот угол.<br>
<br>
Нет ветра сегодня, нет мочи стоять.<br>
Согрелась вода, душно, жарко, -<br>
Термометр поднялся на сорок пять.<br>
Без воздуха вся кочегарка.<br>
<br>
Окончив кидать, он напился воды — <br>
Воды опресненной, не чистой, <br>
С лица его падал пот, сажи следы. <br>
Услышал он речь машиниста:<br>
<br>
«Ты, вахты не кончив, не смеешь бросать, <br>
Механик тобой недоволен.<br>
Ты к доктору должен пойти и сказать, — <br>
Лекарство он даст, если болен!»<br>
<br>
За поручни слабо хватаясь рукой,<br>
По трапу наверх он взбирался;<br>
Идти за лекарством в приемный покой<br>
Не мог – от жары задыхался.<br>
<br>
На палубу вышел, - сознанья уж нет. <br>
В глазах его все помутилось,<br>
Увидел на миг ослепительный свет,<br>
Упал... Сердце больше не билось…<br>
<br>
К нему подбежали с холодной водой,<br>
Стараясь привесть его в чувство,<br>
Но доктор сказал, покачав головой:<br>
«Бессильно здесь наше искусство…»<br>
<br>
Всю ночь в лазарете покойник лежал,<br>
В костюме матроса одетый;<br>
В руках на груди крест из воску лежал;<br>
Воск таял, жарою согретый.<br>
<br>
Проститься с товарищем утром пришли <br>
Матросы, друзья кочегара. <br>
Последний подарок ему поднесли -<br>
Колосник обгорелый и ржавый.<br>
<br>
К ногам привязали ему колосник, <br>
В простыню его труп обернули;<br>
Пришел пароходный священник-старик,<br>
И слезы у многих сверкнули.<br>
<br>
Был чист, неподвижен в тот миг океан,<br>
Как зеркало воды блестели,<br>
Явилось начальство, пришел капитан,<br>
И «вечную память» пропели.<br>
<br>
Доску приподняли дрожащей рукой,<br>
И в саване тело скользнуло,<br>
В пучине глубокой, безвестной морской<br>
Навеки, плеснув, утонуло.<br>
<br>
Напрасно старушка ждет сына домой;<br>
Ей скажут, она зарыдает... <br>
А волны бегут от винта за кормой,<br>
И след их вдали пропадает.<br>
