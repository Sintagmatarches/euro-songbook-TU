<br>
  *Не расшифровывается. По смыслу подходит, например: «В Гергебиле погулять»</span><br>
  <br>
  <br>
  В летнюю кампанию 1847 года главнокомандующий русскими войсками на Кавказе Воронцов 
  планировал очистить от Шамиля Центральный Дагестан, разрушив укрепленные аулы 
  Гергебиль, Салты, Сугур и, по возможности, Ириб. Штурм Гергебиля 14-20 июня 
  провалился. Единственным успехом кампании было взятие после полуторамесячной 
  осады аула Салты 26 сентября (14 сент. ст. ст.). Генерал Аргутинский участвовал 
  в штурме и был ранен. В следующем году его частям удалось взять Гергебиль, а 
  еще через год разрушить укрепленный аул Чох, но ожидаемого эффекта кампания 
  не принесла: горцам каждый раз удавалось вырваться из окруженных аулов, а после 
  ухода русских аулы отстраивались вновь.<br>
