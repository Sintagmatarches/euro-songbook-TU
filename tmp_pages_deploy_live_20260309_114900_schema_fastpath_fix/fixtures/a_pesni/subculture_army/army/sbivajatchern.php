	<h1>СБИВАЯ 
ЧЕРНЫМ САПОГОМ </h1>
<br>
Ну что же ты наделала, надела платье белое.<br>
Кольцо на руку правую, на голову фату.<br>
А разве ты забыла, что ты мне говорила:<br>
Мой милый, мой хороший, верь, я тебя люблю.<br>
<br>
<em>Припев:</em><br>
<br>
Сбивая черным сапогом с травы прозрачную росу,<br>
Погранотряд идет в дозор, и каждый к своему посту.<br>
И каждый думает о той, что где-то ждет и письма пишет:<br>
- Любимый, дорогой, - твои слова я слышу.<br>
<br>
Когда шинель солдатскую с усталых плеч снимаем,<br>
Мы милых и хороших так часто вспоминаем.<br>
И снятся нам заранее леса-дороги дальние<br>
И девушки любимые, что обещали ждать.<br>
<br>
<em>Припев.</em><br>
<br>
Мне часто будет сниться, как свадьба веселится.<br>
Счастливые их лица и «Горько!» все кричат.<br>
Как ждать ты обещала, но слово не сдержала.<br>
Лишь только написала – ты все поймешь, солдат.<br>
<br>
