<span class='bold'>ВАРИАНТ<br>
<br>
Платье белое </span><br>
<br>
Зачем ты это сделала, <br>
  Надела платье белое,<br>
  Кольцо на руку тонкую,<br>
  На голову фату?<br>
  А, может, ты забыла, <br>
  Как часто говорила,<br>
  Как часто говорила,<br>
  Что я тебя люблю. <br>
<br>
<em>Припев: </em><br>
<br>
Сбивая черным сапогом <br>
  С травы прозрачную росу,<br>
  Наш караул идет тропой,<br>
  И каждый к своему посту.<br>
  И каждый думает о том, <br>
  Что дома ждут, что дома пишут:<br>
  «Любимый, милый, дорогой,<br>
  Тебя я жду, тебя я слышу». <br>
<br>
Когда шинель снимают<br>
  С усталых плеч<br>
  О милых вспоминают.<br>
  Они ложатся спать,<br>
  И снятся им родные<br>
  Поля, леса густые,<br>
  И девочки, которые<br>
  Их обещали ждать. <br>
<br>
Теперь мне часто снятся, <br>
  Как свадьбы веселятся.<br>
  Знакомые их лица,<br>
  И «горько» им кричат.<br>
  Любить ты обещала,<br>
  Но слово не сдержала,<br>
  А просто написала:<br>
  «Ты все поймешь, солдат». <br>
