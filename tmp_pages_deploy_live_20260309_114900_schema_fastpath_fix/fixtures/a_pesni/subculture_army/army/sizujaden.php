	<h1>СИЖУ 
Я ДЕНЬ, СИЖУ НЕДЕЛЮ...</h1>
<br>
Сижу я день, сижу 
  неделю,<br>
  А на свиданье ты нейдешь…<br>
  На свиданье приходила<br>
  Убита горем мать моя,<br>
  Печальну весть она сообщила,<br>
  Что баба скурвилась твоя.<br>
  Она с тобой жить не хотела,<br>
  Себе любовника нашла, -<br>
  Шинель твою для этой цели<br>
  В крови испачкала сама.<br>
  Чтобы сурово был наказан,<br>
  В отдел особый донесла.<br>
  И не думай ты, злодейка,<br>
  Что здесь в тюрьме я пропаду:<br>
  Когда выйду я на волю,<br>
  За все за это отомщу.<br>
  Тебе сломаю руки-ноги<br>
  И все косточки намну,<br>
  Живу в могилу блядь зарою,<br>
  А сам опять пойду в тюрьму.<br>
  Все тюремные вороты<br>
  Для нас отворены,<br>
  И тяжкие работы<br>
  На нас возложены.<br>
 <br>
 <span class='i'>В нашу гавань 
  заходили корабли. Вып. 3. М.: Стрекоза, 2000.</span><br>
