Особые отделы - органы контрразведки в Красной, а затем Советской армии, - выполняли роль полиции для военных. Созданы 19 декабря 1918 г. путем объединения  фронтовых и армейских ЧК с органами Военного контроля. Высшим их органом  был Особый отдел ВЧК при СНК РСФСР.<br>
<br>
 В 1934 г. Особый отдел вошел в состав Главного управления государственной безопасности (ГУГБ) НКВД СССР и затем был переименован в 5-й Отдел. В марте 1938 г. с упразднением ГУГБ на базе 5-го Отдела создали 2-е Управление (особых отделов) НКВД СССР. В сентябре 1938 г. Особый отдел был реанимирован как 4-й Отдел ГУГБ. <br>

