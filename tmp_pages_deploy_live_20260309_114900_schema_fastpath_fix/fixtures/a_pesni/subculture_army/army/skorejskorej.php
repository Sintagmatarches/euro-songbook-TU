	<h1>СКОРЕЙ, СКОРЕ 
БЫ БЫЛО ТЕМНО </h1>
<br>Скорей, скоре бы было темно,<br>
  Скорей залетку увидать.<br>
  За лесом солнышко садилось,<br>
  Я собиралася гулять.<br>
<br>Ко всем девчатам шли мальчишки,<br>
  Мово мальчишки не видать.<br>
  Сажусь и думать начинаю:<br>
  «Ой, почему ж он не пришел?<br>
<br>Наверно, он другую любит<br>
  И на свиданье к ней ушел.<br>
  Он обещал на мне жениться,<br>
  Он обещал меня любить.<br>
<br>Наверно, он другую любит<br>
  И на свиданье к ней ушел.<br>
  Он обещал на мне жениться,<br>
  Он обещал меня любить.<br>
<br>Но все мечты его пропали –<br>
  Уходит в армию служить.<br>
  За что прошли мои три года,<br>
  За что прошла моя любовь?<br>
<br>Ой, лучше б я тебя не знала<br>
  И невлюбленная была.<br>
  Мое бы сердце не болело,<br>
  И я б спокойная была.<br>
<br>Теперь ты, милый, уезжаешь,<br>
  Возьми, возьми меня с собой». –<br>
  «Я взял бы, взял тебя с собою,<br>
  Но ты запросишься домой.<br>
<br>У нас в казарме грязно, сыро<br>
  И нет постели пуховой.<br>
  Ложимся спать на голы нары,<br>
  Шинель положим в головах, -<br>
<br>Там одеваться остаются<br>
  Одни худые рукова.<br>
  В чужом краю чужие люди;<br>
  Чужая мать, чужой отец.<br>
<br>Чужая маменька не скажет:<br>
  «Вставай, сынок, попей чайку!»<br>
  Чужой папашенька не скажет:<br>
  «Вставай, сыночек, похмелю!»<br>
<br>А чужой девушке не скажешь:<br>
  «Пойдем до дому провожу».<br>
  Конец, конец веселой песне,<br>
  Спасибо гармонисту за игру.<br>
<br>
<span class='i'>В нашу гавань заходили корабли. Вып. 2. М.: Стрекоза, 2000.</span><br>
