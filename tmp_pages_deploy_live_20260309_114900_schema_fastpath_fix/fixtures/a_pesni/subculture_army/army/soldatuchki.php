	<h1>СОЛДАТУШКИ 
</h1>
<br>
— Солдатушки, бравы ребятушки, <br>
Где же ваши пушки? <br>
— Наши пушки — жёны да подружки, <br>
Вот где наши пушки! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
Где же ваши сёстры? <br>
— Наши сёстры в банке «Коза-Ностры», <br>
Вот где наши сёстры! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
Где же ваши бабы? <br>
— Коллективно выяснить пора бы, <br>
Где же наши бабы! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
Где же ваши жёны? <br>
— Наши жёны в рынок запряжёны, <br>
Вот где наши жёны! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
Где же ваши тёщи? <br>
— Вы спросите что-нибудь попроще, <br>
Вот где наши тёщи! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
А где ваши тётки? <br>
— Мы расскажем после рюмки водки, <br>
Где там наши тётки! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
А где ваши милки? <br>
— Мы расскажем опосля бутылки, <br>
Где там наши милки! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
А где ваши леди? <br>
— В том краю, где белые медведи, <br>
Вот где наши леди! <br>
<br>
— Солдатушки, бравы ребятушки, <br>
Где же ваши дамы? <br>
— Наши дамы с нами за столами, <br>
Вот где наши дамы! <br>
Наши дамы с нами за столами, <br>
Вот где наши дамы! <br>
<br>
Ложись! <br>
