	<h1>СОСНА </h1>
  <br>
  Стоит сосна<br>
  Река жемчужная течет<br>
  А вдоль реки парнишка с девушкой идет.<br>
  Они идут незамедляя в жизни шаг<br>
  И вдруг у парнишки появились слезы на глазах.<br>
  <br>
  [колючая проволока]<br>
  <br>
  Я ухожу сказал он ей сквозь боль и грусть.<br>
  Я ухожу, но обязательно вернусь.<br>
  И он ушел, не встретив первую весну.<br>
  Домой пришел в солдатском цинковом гробу.<br>
  <br>
  [колючая проволока]<br>
  <br>
  Рыдает мать и словно тень стоит отец<br>
  Ведь он для них еще по прежнему юнец<br>
  А сколько их не зделав в жизни шаг<br>
  Домой пришли в солдатских цинковых гробах<br>
  <br>
  [колючая проволока]<br>
  <br>
  Он как и все любил с девченками гулять<br>
  Дарил цветы и на гитаре им играл.<br>
  И в тот же миг когда на белый снег упал<br>
  Он имя той девченки кровью написал<br>
  <br>
  [колючая проволока]<br>
  <br>
  Рассеет ветер на горизонтом серый дым<br>
  Девченка та что обещала подожду<br>
  Девченка та уже целуется с другим<br>
  Пришла весна исчезло имя на снегу.<br>
  <br>
