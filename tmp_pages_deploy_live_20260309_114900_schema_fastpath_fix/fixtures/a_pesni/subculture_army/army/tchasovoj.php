	<h1>ЧАСОВОЙ 
</h1>
<br>
Музыка Владислава Алоиза<br>
Слова Ивана Сурикова<br>
<br>
Полночь. Злая стужа <br>
На дворе трещит. <br>
Месяц облаками <br>
Серыми закрыт. <br>
<br>
У большого зданья <br>
В улице глухой <br>
Мерными шагами <br>
Ходит часовой. <br>
<br>
Под его ногами <br>
Жесткий снег хрустит, <br>
А кругом глухая <br>
Улица молчит. <br>
<br>
Но шагает ровно <br>
Бравый часовой, <br>
И ружье он крепко <br>
Жмет к плечу рукой. <br>
<br>
Вспомнился солдату <br>
Край его родной; <br>
Вспомнилась избушка<br>
С белою трубой;<br>
<br>
Вспомнилась голубка,<br>
Милая жена:<br>
Чай, теперь на печке<br>
Спит давно она.<br>
<br>
Может быть, ей снится,<br>
Как мороз трещит,<br>
Как солдат озябший<br>
На часах стоит.<br>
<br>
1864, слова<br>
1903, музыка<br>
