	<h1>ТОП-ТОП 
</h1>
<br>
Топ-топ... Очень нелегки <br>
С раннего подъёма сапоги. <br>
Старшина кричит: «Быстрее в строй!», <br>
А солдату хочется домой. <br>
<br>
Топ-топ... все мы встали в строй: <br>
Кто стоит раздетый, кто — босой, <br>
У кого совсем портянок нет – <br>
Кухня «улыбается» в ответ. <br>
<br>
Топ-топ... С плаца мы идём, <br>
Еле-еле ноги волокём. <br>
«Выше ногу!» — нам кричит сержант. <br>
Проклинает жизнь свою курсант. <br>
<br>
Топ-топ... <br>
За столами целый день сидим, <br>
Как баран, в учебники глядим. <br>
А девчонку вспомнишь в этот миг — <br>
И солдату сразу не до книг... <br>
<br>
Топ-топ... Топает малыш <br>
С мамой по дорожке, милый стриж. <br>
Маленькие ножки не спешат, <br>
А курсанты смотрят и твердят: <br>
<br>
«Топ-топ... Скоро подрастёшь <br>
И на смену нам тогда придёшь! <br>
И сполна узнаешь ты, малыш, <br>
Как трудна у нас, курсантов, жизнь». <br>
Топ-топ... <br>
Топ-топ... <br>
Топ-топ... <br>
