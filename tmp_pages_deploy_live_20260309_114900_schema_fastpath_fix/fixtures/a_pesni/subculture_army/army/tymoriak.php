  &quot;Куплеты моряка&quot; из драмы Василия Межевича (1814-1849)  &quot;Артур, или Шестнадцать лет спустя&quot; (1839). В драме пелись моряком с хором. Первоначальная мелодия неизвестна. До XX в. песня дошла благодаря тому, что вошла в печатные песенники, и перепечатывалась из года в год.<br>
  <br>
  Песня повсеместно бытовала в годы Первой мировой и Гражданской 
  войн. По свидетельству Фурманова, была одной из любимых песен Чапаева: 
  &quot;Любил ее Чапаев больше всего за припев – так он паялся хорошо с этой партизанской 
  кочевою, беспокойной жизнью&quot;. В 1920-е гг. появился вариант для красного флота, в 1941 г. Лебедев-Кумач написал новый текст для Великой Отечественной войны.<br>
