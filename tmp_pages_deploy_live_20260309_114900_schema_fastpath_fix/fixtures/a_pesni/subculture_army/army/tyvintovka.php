	<h1>ТЫ 
ВИНТОВКА </h1>
<br>
Ты, винтовка, друг мой верный, <br>
Кто служить с тобой не рад! <br>
<br>
Это правда, это правда, <br>
Кто служить с тобой не рад! <br>
<br>
И тобой гордится смело <br>
Царя русского солдат, <br>
<br>
Это правда, это правда. <br>
Царя русского солдат. <br>
<br>
Ты наружностью красива, <br>
Нас прославила собой. <br>
<br>
Это правда, это правда, <br>
Нас прославила собой. <br>
<br>
Ты нам славу подарила <br>
Превосходною стрельбой. <br>
<br>
Это правда, это правда, <br>
Превосходною стрельбой. <br>
<br>
Наведу я твою мушку <br>
В отдаленный белый щит. <br>
<br>
Это правда, это правда, <br>
В отдаленный белый щит. <br>
<br>
Пуля с визгом отскочила, <br>
Смотрят: круг уже пробит. <br>
<br>
Это правда, это правда, <br>
Смотрят: круг уже пробит. <br>
<br>
Дам я выстрел, другой, третий - <br>
Дело к лучшему идет. <br>
<br>
Это правда, это правда, <br>
Дело к лучшему идет. <br>
<br>
Капитан сейчас отметит <br>
И домой меня пошлет. <br>
<br>
Это правда, это правда, <br>
И домой меня пошлет. <br>
<br>
Взяв винтовку, шагом мерным <br>
Путь в палатку я держал.<br>
<br>
Это правда, это правда, <br>
Путь в палатку я держал. <br>
<br>
Ус курчавый, глаз чернявый, <br>
Сам с собою рассуждал. <br>
<br>
Это правда, это правда, <br>
Сам с собою рассуждал. <br>
<br>
Как приду с тобой на место, <br>
Разберу, мой друг, тебя. <br>
<br>
Это правда, это правда, <br>
Разберу, мой друг, тебя. <br>
<br>
И прозрачною водою <br>
Смою чисто, добела. <br>
<br>
Это правда, это правда, <br>
Смою чисто, добела. <br>
<br>
Не грусти ж, моя родная, <br>
В черном, траурном чехле. <br>
<br>
Это правда, это правда, <br>
В черном траурном чехле. <br>
<br>
Завтра ж, с утренней зарею, <br>
С нами будешь на стрельбе. <br>
<br>
Это правда, это правда, <br>
С нами будешь на стрельбе. <br>
