	<h1>УРА! 
ГВАРДЕЙСКИЕ УЛАНЫ </h1>
<br>
Ура! Гвардейские уланы! <br>
Кто не слыхал про молодцов?<sup>1</sup><br>
Недаром помнят басурманы <br>
Про наших дедов и отцов! <br>
<br>
Недаром кровью и трудами <br>
Мы заслужили у Царя <br>
Штандарты с белыми крестами <br>
И трубы - все из серебра. <br>
<br>
Кто не слыхал, как лейб-уланы <br>
Дрались в двенадцатом году? <br>
То были вихри, ураганы, <br>
Беда была им не в беду. <br>
<br>
Бывало, дня не проходило, <br>
Чтоб наши не были в огне, <br>
Чтоб утро их не захватило<br>
Готовых к бою, на коне. <br>
<br>
И вихрем мчались лейб-уланы <br>
На басурманские ряды, <br>
И кровью смоченные раны <br>
Обозначали их следы. <br>
<br>
Хотя теперь меж наших хватов <br>
Не видно прежних стариков, - <br>
Но в их преемниках-собратах <br>
Все тот же дух, все та же кровь! <br>
<br>
Так выступайте ж, басурманы! <br>
Мы вам покажем в добрый час, <br>
Что за народец лейб-уланы, <br>
Что значит гвардия у нас. <br>
<br>
