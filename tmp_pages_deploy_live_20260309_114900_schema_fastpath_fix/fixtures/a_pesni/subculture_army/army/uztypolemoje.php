Уж ты, поле мое, поле чистое, <br>
Ты раздолье мое, ты широкое.<br>
<br>
Ничего ты, поле, не спородило, <br>
Спородило поле част ракитов куст. <br>
<br>
Как во этом кусту тело белое, <br>
Тело белое, молодецкое. <br>
<br>
Во главах у него сабля вострая, <br>
В ретивом у него пуля быстрая, <br>
<br>
Во ногах у него конь вороной его. <br>
«Уж ты, конь, ты мой конь, ты, товарищ мой, <br>
<br>
Ты беги, беги по дорожке вдоль, <br>
По дорожке вдоль, к отцу, к матери, <br>
<br>
К отцу, к матери, к молодой жене. <br>
Ты скажи жене, что женился я,<br>
<br>
Что женила меня пуля быстрая, <br>
Обвенчала меня сабля вострая». <br>
<br>
<span class='i'>Такун Ф.И. Славянский базар. М.: Современная музыка, 2005.</span><br>

