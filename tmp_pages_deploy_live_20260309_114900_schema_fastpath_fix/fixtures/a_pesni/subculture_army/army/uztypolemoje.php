	<h1>УЖ 
ТЫ ПОЛЕ МОЕ, ПОЛЕ ЧИСТОЕ...</h1>
<br>
«Уж ты поле мое, поле чистое <br>
Ты раздолье мое, ты широкое. <br>
<br>
Ты раздолье мое, ты широкое, <br>
Чем ты, полюшко, приукрашено?»<br>
<br>
— «А я, полюшко, всё цветочками, <br>
Всё цветочками, василечками!» <br>
<br>
Посреди-то поля част ракитов куст, <br>
Под кустом-то лежит тело белое.<br>
<br>
Под кустом-то лежит тело белое, <br>
Тело белое, молодой солдат. <br>
<br>
Молодой солдат не убит лежит <br>
Не убит лежит, шибко раненный. <br>
<br>
Во главах у него бел-горюч камень, <br>
Во руках у него сабля вострая, <br>
<br>
Во руках у него сабля вострая, <br>
Во груди у него пуля быстрая, <br>
<br>
Во груди у него пуля быстрая, <br>
Во ногах у него стоит добрый конь. <br>
<br>
«Уж ты конь, ты мой конь, товарищ мой, <br>
Ты ступай-беги во Русску землю; <br>
<br>
Ты скажи-ка, скажи родному батюшке, <br>
Поклонись-ка ты родной матушке,<br>
<br>
Поклонись-ка ты родной матушке. <br>
Ты скажи-ка, скажи молодой жене,<br>
<br>
Что женился я на другой жене, <br>
Как женил-то меня бел горюч камень, <br>
<br>
Как женил-то меня бел горюч камень, <br>
Обвенчала-то меня сабля вострая,<br>
<br>
Обвенчала-то меня сабля вострая, <br>
Молода жена пуля быстрая».<br>
<br>
<span class='i'>Русские народные песни / Сост. и вводн. тексты В.В. Варгановой. 
М.: Правда, 1988. Раздел «Песни солдат и о солдатах. Рекрутские причитания».</span><br>
