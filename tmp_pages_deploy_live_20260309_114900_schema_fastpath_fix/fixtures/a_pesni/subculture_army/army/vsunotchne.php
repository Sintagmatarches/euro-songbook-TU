	<h1>ВСЮ НОЧЬ НЕ СПАЛ, ВСЁ ХОДИЛ ДА ГУЛЯЛ...</h1>
<br>
	Всю ночь не спал, всё ходил да гулял; <br>
  Ко белой-то заре ко двору я пришел. <br>
  Молодая жена со постели встает, <br>
  Со постели встала, слезой залилась: <br>
  «Без тебя, мой друг, постелюшка холодна, <br>
  И одеялице призаинело в ногах, <br>
  Подушечка потонула во слезах». <br>
  Со стыда молодец, я вниз личиком упал, <br>
  Сам себя сругал, про гуляньице сказал: <br>
  «Ты гулянье, ты гуляньце мое! <br>
  Довело ты молодца до конца! <br>
  Распрогневал я родную матушку, отца; <br>
  Через то, младец, на службицу пошел. <br>
  А на службице последнее житье; <br>
  По немногу там нам жалованья дают: <br>
  По три денежки у суточки, <br>
  По пяти плетей у спинушку кладут! <br>
  Ты не плачь, ты не плачь, родимая матушка!<br>
  Не плачь, не печаль доброго молодца, <br>
  Ты не жди, ты не жди младца через три года; <br>
  На четвертый год сам письмо пришлю, <br>
  Во письме пропишу строку черную, <br>
  Строку черную, печальную; <br>
  Поминай ты меня тогда, родимая матушка, хлебом-солью». <br>
  <br>
