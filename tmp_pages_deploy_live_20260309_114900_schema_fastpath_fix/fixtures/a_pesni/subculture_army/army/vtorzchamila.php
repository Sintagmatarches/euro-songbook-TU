<h1>ВТОРЖЕНИЕ 
ШАМИЛЯ В КАБАРДУ (1846 г.)</h1>
Песня Кабардинского Егерского полка <br>
<br>
Двадцать третьего апреля<br>
На рассвете бела дня<br>
С горя песенки запели<br>
Про неверна Шамил<strong>я</strong>.<br>
<br>
<em>Припев:</em><br>
<br>
Гремит слава трубой,<br>
Мы дралися за Лаб<strong>о</strong>й.<br>
По горам твоим, Кавказ,<br>
Раздалась слава про нас!<br>
<br>
Вы, черкесы, не бранитесь,<br>
Мы вам место подберем – <br>
За Кубанью за рекой<br>
Мы дороженьки прошли.<br>
<br>
Прошли тёмны там леса,<br>
Нам помогут небеса,<br>
Мы туда путь пролагали<br>
По деревням, по горам.<br>
<br>
По деревням, по горам,<br>
Где мы проходили,<br>
Теперь там наш генерал <br>
Голицын со отрядом.<br>
<br>
Он проводил весь отряд<br>
По лесам и горам,<br>
Он проводил так отряд,<br>
Чтобы войска не терять.<br>
<br>
Генерал наш Ушаков,<br>
Повел Егерьевский полк.<br>
Он не любит перестрелки<br>
Прямо ломит на штыки!<br>
<br>
Раным-рано на заре<br>
Соловейка просвистал,<br>
Соловейка просвистал,<br>
Нам дорожку указал.<br>
<br>
Двадцать третьего апреля<br>
На рассвете бела дня<br>
С горя песенки запели<br>
Про неверна Шамил<strong>я</strong>.<br>
<br>
<span class='i'>Расшифровка фонограммы Мужского хора Института певческой культуры 
«Валаам», дирижер И. Ушаков, запись 1997. CD «Песенная летопись Кавказской войны», 
СПб, Русская Лира, 2002.<br></span>
<br>
<br>
Запев <em>&quot;Двадцать третьего апреля На рассвете бела дня...&quot;</em> перешел 
из более ранней песни о бое под Андреевой деревней 23 апреля (ст. ст.) 1844 г. 
(см. <a href="dpodandr.php">&quot;Дело под Андреевой деревней&quot;</a>). Но по 
смыслу он подходит - вторжение Шамиля в Кабарду для подъема кабардинцев на восстание 
тоже произошло в апреле: 25 апреля (нов. ст.) Шамиль вышел из Шали, 9 мая ушел 
из Кабарды. Кабардинцы не восстали.<br>
<br>
<em>Л<strong>а</strong>ба</em> - река в районе Кубанской линии укреплений, приток 
Кубани. В 1840 г. вдоль нее была сооружена Лабинская линия. <em>Черкесы</em> - 
черкесами в во времена Кавказской войны 1817-1864 гг. называли любых горцев, которые 
воевали с русскими.<br>

