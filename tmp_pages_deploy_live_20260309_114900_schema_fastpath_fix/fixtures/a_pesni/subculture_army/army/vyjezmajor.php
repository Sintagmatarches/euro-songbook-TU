	<h1>ВЫЕЗЖАЕТ МОЛОДОЙ 
МАЙОР…</h1>
<br>Выезжает молодой майор, <br>
  Молодой майор-полковничек, <br>
  Выбирает молодых солдат, <br>
  Молодых солдат-ребятушек. <br>
  У отца было три сына, <br>
  Три сына хорошие; <br>
  Большого сына жаль отдать, <br>
  Среднего не хочется; <br>
  Отдам я сына меньшего, <br>
  Сына меньшего, Иванушку! <br>
  Меньший сын расплакался: <br>
  «Государь наш батюшка! <br>
  Или я вам был не родный сын, <br>
  Или я вам был пасынок?» — <br>
  «Да вы, дети мои милые! <br>
  Вы возьмите-тко по палочке, <br>
  Да вы бросьте-тко по жеребью: <br>
  Да кому из вас достанется?» <br>
  Доставалось сыну меньшему, <br>
  Сыну меньшему, Иванушке. <br>
<br>
