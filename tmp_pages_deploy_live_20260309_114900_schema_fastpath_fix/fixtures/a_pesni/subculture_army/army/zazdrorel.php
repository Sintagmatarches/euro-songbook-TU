  Гавриил Державин начал свою карьеру в 1862 г. солдатом Преображенского полка. Момент 
  оказался очень удачным: в этом же году преображенцы отличились в дворцовом перевороте 
  Екатерины II. На военной службе он провел более 10 лет. А впоследствии стал 
  известным поэтом, статс-секретарем Екатерины и сенатором.<br>

