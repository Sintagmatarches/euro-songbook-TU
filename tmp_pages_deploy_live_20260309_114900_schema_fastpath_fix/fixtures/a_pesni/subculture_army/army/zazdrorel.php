Слова Гавриила Державина<br>
<br>
По северу, по югу <br>
С Москвы Орел парит; <br>
Всему земному кругу <br>
Полет его звучит. <br>
<br>
О! исполать, ребяты, <br>
Вам, русские солдаты! <br>
Что вы неустрашимы, <br>
Никем непобедимы: <br>
<div class='refren'>За здравье ваше пьем. </div>
<br>
Орел бросает взоры <br>
На Льва и на Луну, <br>
Стокгольмы и Босфоры - <br>
Все бьют челом ему. <br>
<br>
О! исполать вам, вон, <br>
Бессмертные герои, <br>
Румянцев и Суворов! <br>
За столько славных б<strong>о</strong>ев<br>
<div class='refren'>Мы в память вашу пьем. </div>
<br>
Орел глядит очами <br>
На солнце в высоты. <br>
Герои под шлемами <br>
На женски красоты. <br>
<br>
О! исполать, красотки, <br>
Вам, росски амазонки! <br>
Вы в мужестве почтенны, <br>
Вы в нежности любезны: <br>
<div class='refren'>За здравье ваше пьем!</div>
<br>
1795 <br>
