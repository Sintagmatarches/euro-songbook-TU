<h1>ЖМЕТ, 
ЩЕМИТ КРУЧИНА...</h1>
<br>
Жмет, щемит кручина <br>
Сердце ретивое, <br>
Нет душе разгульной <br>
От тоски покоя!.. <br>
<br>
Припев: <br>
<br>
За морем живут <br>
Девушки красотки; <br>
Белые лебедки; <br>
Девок парни любят, <br>
Птиц стрелы губят!.. <br>
Ай, люлю-люли! <br>
Люли-люшеньки мои! <br>
<br>
Так с тоски да с горя <br>
Полетимте, братцы, <br>
В край мы Закаспийский <br>
С текинцами драться! <br>
<br>
Припев. <br>
<br>
Полетим дружнее <br>
К синему мы морю <br>
И тоску-злодейку <br>
Сбросим на просторе!.. <br>
<br>
Припев. <br>
<br>
Спустим наши струги <br>
В волны голубые <br>
И ударим в весла, <br>
Весла расписные! <br>
<br>
Припев. <br>
<br>
А затем на берег <br>
Высадимся тучей <br>
И помчимся степью <br>
Конницей летучей!.. <br>
<br>
Припев. <br>
<br>
Пусть ведет нас степью <br>
Царь тропинкой узкой <br>
В горы и ущелья, <br>
Богатырь наш русский. <br>
<br>
Припев. <br>
<br>
Мощный и отважный <br>
Генерал наш Белый, <br>
Скобелев удалый, <br>
Полководец смелый! <br>
<br>
Припев. <br>
<br>
Разгромим текинцев, <br>
На Афган нагрянем, <br>
И всем азиатцам <br>
Мы грозою станем. <br>
<br>
Припев. <br>
<br>
Заберем сартянок <br>
В плен их чернооких; <br>
Пусть об нас все знают <br>
Средь степей широких! <br>
<br>
Припев. <br>
<br>
А затем обратно, <br>
Перелетной птицей, <br>
Из степей вернемся <br>
В вольные станицы. <br>
<br>
Припев. <br>
<br>
Заживем, как прежде <br>
Жили наши деды: <br>
Пили, веселились, <br>
Празднуя победы! <br>
<br>
Припев. <br>
<br>
Так в поход смелее! <br>
Братцы, не робейте! <br>
Рук своих могучих <br>
В битвах не жалейте! <br>
<br>
Припев. <br>
