Казачья песня эпохи завоевания Средней Азии (1850-е - 1880-е гг.). Генерал Михаил Скобелев был назначен командующим Ахал-текинской экспедиции (на территорию современной Туркмении) в январе 1880 г.; в январе 1881 г. он взял крепость Геок-Тепе.  Операции в Туркмении закончились принятем русского подданства Мервом в 1884 г. и боем с афганцами в 1885 г. за сохранение контроля над ним.<br>

