<h1>ЗНАЮТ 
  ТУРКИ НАС И ШВЕДЫ</h1>
  Неизвестные авторы<br>
  <br>
  Знают турки нас и шведы, <br>
  И про нас известен свет, <br>
  На сраженьях, на победы <br>
  Нас всегда сам Царь ведет. <br>
  <br>
  С нами труд Он разделяет, <br>
  Перед нами Он в боях;<br>
  Счастьем всяк из нас считает <br>
  Умереть в Его глазах. <br>
  <br>
  Славны были наши деды, - <br>
  Помнит их и швед и лях; <br>
  Их парил орел победы <br>
  На Полтавских на полях. <br>
  <br>
  Знамя их полка пленяет<sup>1</sup><br>
  Русский штык наш боевой; <br>
  Он и нам напоминает, <br>
  Как ходили деды в бой! <br>
  <br>
  Тверд еще наш штык граненый, <br>
  Голос чести не замолк. <br>
  Как пойдем вперед мы славно, <br>
  Вперед, славный русский полк. <br>
  <br>
  Государям по присяге <br>
  Верным полк наш был всегда, - <br>
  В поле брани, не робея, <br>
  Грудью служит завсегда. <br>
  <br>
  Как и прежде - удалые, <br>
  Рады тешить мы Царя, <br>
  И солдаты боевые <br>
  Славны будут ввек, ура! <br>
  <br>
  <sup>1</sup>Пленяет - берет в плен. <br>
