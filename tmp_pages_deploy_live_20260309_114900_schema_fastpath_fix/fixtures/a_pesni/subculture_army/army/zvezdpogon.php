	<h1>ЗВЕЗДА НА 
ПОГОНАХ </h1>
<br>
Не карьеры ради выбирают<br>
  Ратной службы трудную стезю.<br>
  Полигоны судьбы отмеряют<br>
  И солдатам, и офицерью.<br>
<br>
<em>Припев:</em><br>
<br>
Ведь звезда на золото погонов<br>
  Нам с небес не падает, увы.<br>
  На ней пыль и слякоть полигонов,<br>
  Чья-то смерть, контузии и швы.<br>
<br>
Помню я седого капитана,<br>
  По годам он был ровесник мой.<br>
  На сухой земле Афганистана<br>
  Часто бредил он родной Москвой.<br>
<br>
<em>Припев.</em><br>
<br>
Офицерский век в бою короче<br>
  На порядок жизни на земле.<br>
  Новый выбьют чин на позолоте<br>
  На могильной каменной плите.<br>
<br>
<em>Припев:</em><br>
<br>
Ведь звезда на золото погонов<br>
  Нам с небес не падает, увы.<br>
  На ней пыль и слякоть полигонов,<br>
  Наша смерть, контузии и швы.<br>
<br>
