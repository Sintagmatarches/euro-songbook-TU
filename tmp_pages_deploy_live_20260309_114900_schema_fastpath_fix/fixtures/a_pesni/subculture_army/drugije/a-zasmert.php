  Александр Бобров, <br>
  лауреат премии им. А. Фатьянова "Соловьи, соловьи..." <br></span>
 
  <h1>&quot;ЗА СМЕРТЬЮ ИЛЬ ЗА СЛАВОЙ...&quot;</h1>
 
  <span class='i'>Предисловие к книге: Священная война. Песни Победы / Сост. В.А. Костров, 
  Г.Н. Красников. М.: Олимп, 2005. С. 3-12.<br></span>
  <br>
  <br>
  Поэзия ратного подвига, воинская песня доходит до нас эхом из глубины веков, 
  уводит к штокам походов "за смертью иль за славой", как писал еще Василий Жуковский.<br>
  <br>
  В древнем городе Трубчевске, над могучей поймой Десны стоит в парке замечательный 
  памятник Бояну. Древний певец считается родом из соседнего, теперь тоже незалежного 
  от России, Чернигова, но на Брянщине дерзко и дальновидно зачислили сказителя 
  в земляки, потому теперь именно здесь проводится замечательный праздник "Слова 
  о полку Игореве". На коленях у старшего современника и учителя автора "Слова..." 
  - гусли. Ясно, что традиции воинских сказаний и песен были и тогда уже глубоки. 
  Давно доказано, что "Слово…" исполнялось в этой древней традиции как ритмизированная 
  поэма-песня под гусли, где речитатив сменялся пением -треть текста пронизана 
  напевной интонацией и скреплена стихотворным размером. Если первый переводчик 
  "Слова..." Василий Жуковский еще оставляет в зачине "печальную повесть о битвах 
  Игоря", то первый автор вольного поэтического пересказа Аполлон Майков убирает 
  даже само слово "повесть", сразу ввергая великую поэму в песенную стихию:<br>
  <br>
  Не начать ли нашу песнь, о братья,<br>
  Со сказаний о старинных бранях, - <br>
  Песнь о храброй Игоревой рати <br>
  И о нем, о сыне Святославле.<br>
  <br>
  На Москве одним из первых известных авторов стихов и музыки был в XVI веке... 
  Иван Грозный. На стихираре песнопений начертано: "Творения Царя Иоанна, деспота 
  Московского". Тоща слово "деспот" переводилось без эмоциональной окраски - властелин. 
  Этот властелин не только слагал духовные песни и молился, замаливая грехи, но 
  и воспевал свои победы.<br>
  <br>
  Гений Ломоносов, стоявший в России у истоков науки и современной поэзии, сложил 
  первую военно-патриотическую оду "На взятие Хотина":<br>
  <br>
  Крепит отечества любовь <br>
  Сынов российских дух и руку.<br>
  <br>
  Сановный поэт Гавриил Державин не только продолжил одическую традицию в стихах 
  "На взятие Измаила", но и стал автором первой подлинно солдатской песни - "Заздравный 
  Орел", которую можно было петь и у бивуачного костра, и шагая в строю, хоть 
  посвятил стихи графам Суворову и Румянцеву:<br>
  <br>
  По северу, по югу<br>
  С Москвы Орел парит;<br>
  Всему земному кругу<br>
  Полет его звучит. <br>
  О, исполать, ребяты,<br>
  Вам, русские солдаты!<br>
  Что вы неустрашимы,<br>
  Никем не победимы:<br>
  За здравье ваше пьем.<br>
  <br>
  Василий Жуковский - учитель Пушкина продолжает сей тост в пространном произведении 
  "Певец во стане русских воинов", название которого стало поэтическим заветом, 
  выражением сознательной позиции русского поэта в годину тяжких испытаний:<br>
  <br>
  Друзья, прощанью кубок сей!<br>
  И смело в бой кровавый <br>
  Под вихорь стрел, на ряд мечей, <br>
  За смертью иль за славой.<br>
  <br>
  Отечественная война 1812 года породила целую плеяду певцов военной славы - от 
  молодого офицера Федора Глинки до маститого уже Константина Батюшкова. Наконец, 
  в ее горниле родилась гусарская песня как устойчивый жанр. Его родоначальник 
  - Денис Давыдов, которого Пушкин величал: "Ты мой отец и командир". Не мудрствуя 
  лукаво поэт-партизан прямо пишет "Военную песню" - как бы вольное подражание 
  популярному французскому романсу, но какое это русское "подражание" и сколько 
  раз потом, включая наши дни, пелось это стихотворение на оригинальный мотив!<br>
  <br>
  Я люблю кровавый бой!<br>
  Я рожден для службы царской! <br>
  Сабля, водка, конь гусарской,<br>
  С вами век мне золотой!<br>
  <br>
  Первым фанатиком и неустанным творцом песен в народном духе был в парадоксальной 
  России аристократ, барон с немецкой фамилией - Антон Дельвиг, закадычный друг 
  Пушкина. Самая ранняя его "Русская песня", датируется днем Бородина по новому 
  стилю - 7 сентября 1812 года. Еще и Пушкин не публиковал своего стихотворения 
  в "Вестнике Европы". <br>
  <br>
  Ну а что же сам Пушкин? Ведь он-то должен быть в чем-то первым и в этой обширной 
  области ратной поэзии! Да, наш национальный гений первым создает совершеннейшие 
  военно-исторические, и военно-политические произведения, которые преисполнены 
  не только величайшей погони, но и отражают историческим знанием н чутьем, а 
  еще - геополитическими прозрениями. Не зря самостийники, взявшие, за идеал Мазепу, 
  так не любят "Полтаву", а все предатели и безоглядные западники терпеть не могут 
  отповедь "Клеветникам России". Ну и жанр военной песни был освоен Пушкиным безукоризненно. 
  Он называет стихотворение "Песнь о вещем Олеге", и это впрямь - песня, которая 
  становится строевой и боевой на все времена. Особенно часто она звучала в Балканскую 
  и в Первую мировую войны. Верность истории, какой бы трагической она ни была, 
  - один из главных заветов Пушкина.<br>
  <br>
  Романс актера и песельника Николая Цыганова - основоположника актерской песий, 
  современника Пушкина - "Не шеи ты мне, матушка, красный сарафан..." вдруг неожиданно 
  врывается в повесть Некрасова "Макар Осипович Случайный", написанную уже после 
  смерти тридцати пятилетнего актера. В ней один из героев говорит: "Я пишу во 
  всех родах: трагедии, оперы, драмы, водевили. Да, вы еще не читали моего водевиля. 
  Вот он, послушайте! (он взял одну из тетрадей) "Святополк Окаянный", водевиль 
  в одном действии, с куплетами, действие на Арбате, в Москве.<br>
  <br>
  "Но Москвы тогда еще не было?"<br>
  <br>
  Ученым и поэтам все позволено, - отвечал он... Святополк ходит в задумчивости 
  и напевает известную песню: "Не шей ты мне, матушка, красный сарафан..." Замечательно! 
  - и весьма напоминает подход нынешних плодовитых авторов: все позволено. Стало 
  расхожим поводом для шуток произведение одного советского поэта, в котором возлюбленная 
  просит перед уходом на войну казака: "Подари мне, сокол, на прощанье саблю, 
  на прощанье пику подари..." А чем же воевать? - всплывает вопрос.<br>
  <br>
  В годы Гражданской войны неожиданно популярной стала песня учителя словесности 
  Василия Межевнча "Ты, моряк, красивый сам собою..." В 1839 году он редактировал 
  "Литературную газету", в его доме собирались любители пения. Они ли упростили 
  куплеты из водевиля Межевича, сам ли автор переделал, или народ обкатал, но 
  доподлинно известно, что эту песню очень любил тезка автора - Василий Чапаев. 
  Фурманов рассказывает: "Другого такого любителя песен - не сыскать: ему песни 
  были, как хлеб, как вода. И ребята его по дружной привычке, за компанию неугомонную 
  - не отставали от Чапая. Песенка шла до конца такая же растрепанная, пустая, 
  бессодержательная. И любил ее Чапаев больше за припев - он так паялся хорошо 
  с этой партизанской, кочевою, беспокойной жизнью:<br>
  <br>
  По морям, по волнам, <br>
  Нынче здесь, а завтра там! <br>
  Эх, по морям-морям-морям, <br>
  Нынче здесь, а завтра там!"<br>
  <br>
  Кстати, разве это не прототип так называемых туристских, дорожных и солдатских 
  походных песен?<br>
  <br>
  У нас даже романс втягивается в обширную область воинской поэзии. Например, 
  выдающийся романс "Белой акации гроздья душистые" считается переработкой малоизвестного 
  стихотворения А. Пугачева, писавшего цыганские романсы. Вера Панина сама редактировала 
  их тексты. Что мы знаем досконально? Во-первых, это замечательное лирическое, 
  явно личностное стихотворение, во-вторых, его напев использован в революционной 
  песне "Смело мы в бой пойдем", в-третьих, известный поэт-песенник Михаил Матусовский 
  использовал этот романс как парафраз в известной песне - "Целую ночь соловей 
  нам насвистывал...", где строка "Белой акации гроздья душистые" является самой 
  лучшей, повторяется много раз и становится символом некой изысканной белогвардейщины.<br>
  <br>
  В этом же духе написана песня позднего Вертинского, который, кажется, не сам 
  уже песенки сочиняет, а обращается к чьей-то сильной, пронзительной поэзии, 
  перлы которой теряются в унесенной на корнях почве русской эмиграции:<br>
  <br>
  И никто не додумался просто встать на колени<br>
  И сказать этим мальчикам, что в бездарной стране <br>
  Даже светлые подвиги - это только ступени <br>
  В бесконечные пропасти - к недоступной Весне.<br>
  <br>
  И в 80-х, и в 90-х годах XX века перед нашими мальчиками снова разверзались 
  бесконечные пропасти и реальных гор Афгана, и Чечни, и метафорические - предательства, 
  непонимания, забвения, но и - полета духа. Потому-то стала своей для воинов-интернационалистов 
  переделанная песня "Кукушка" солдата Великой Отечественной Виктора Кочеткова, 
  а сами "афганцы" откликнулись авторскими песнями раненого воина Александра Карпенко, 
  военного журналиста Виктора Верстакова, чеченскими, снецназовскими да десантными 
  циклами Дмитрия Московского и актера Николая Сахарова.<br>
  <br>
  Вообще традиции ратной песни, увы, не прерывались, ибо слишком часто русским 
  поэтам и солдатам приходилось воевать. Во время Гражданской и Великой Отечественной 
  войны, пока не хватало вновь сложенных песен, гуляли бесконечные переделки старых 
  и недавних песен. Например, один почитатель таланта Михаила Исаковского прислал 
  поэту сто разных "Катюш" - пограничных, партизанских, тыловых и даже подневольных. 
  А ведь песня, по которой назвали грозное оружие, была сложена еще до войны.<br>
  <br>
  В XX веке с утверждением авторской поэзии, гражданской лирики в нашей стране 
  особую силу и государственное признание приобрела профессиональная массовая 
  песня. Если раньше Лермонтов на территории нынешней Чечни слагал "Казачью колыбельную" 
  с прицелом на проголосное исполнение, но не в ущерб художественной выразительности, 
  то в XX веке появляется вместе с массовой культурой и индустрией идеологизированного 
  творчества амплуа поэта-песенника, подтекстовщика. И это нечисто советское явление 
  - даже в странах, где поэзия ушла в элитарное формотворчество, в верлибр и визуальные 
  эксперименты, пишутся рифмованные, понятные до примитивности тексты песен, смешные 
  куплеты или торжественные гимны. Но Великая Отечественная всколыхнула душу народа, 
  породила великую поэзию и гениальных поэтов-песенников. Достаточно вспомнить 
  "Священную войну" Лебедева-Кумача, "Землянку" Суркова или "Дороги" Ошанина. 
  Но больше всего шедевров в жанре фронтовой лирической песни создал, пожалуй, 
  Алексей Фатьянов - "русской песни запевала и ее мастеровой", как точно отчеканил 
  Ярослав Смеляков.<br>
  <br>
  Обращаясь к гостям одного из самых задушевных праздников поэзии и песни в России 
  - "На солнечной поляночке", глава города Вязники - поющий мэр, как его называют, 
  - Евгений Виноградов весьма уместно напомнил, что Алексей Фатьянов - единственный 
  из поэтов, награжденный боевой наградой именно за песенное творчество. И в этом 
  - высшая справедливость. С его песнями бойцы поднимались в атаку, но что важнее 
  - отходили душой от кровавой, изматывающей работы. Может, и не озверел русский 
  солдат еще и потому, что пел на привале у костра, в землянке такие песни, как 
  "На солнечной поляночке", "Где ж ты, мой сад?", "Горит свечи огарочек...". Да 
  разве все фатьяновское перечислишь!<br>
  <br>
  К счастью Фатьянова, он встретил "своего" соавтора, гениального мелодиста - 
  Василия Соловьева-Седого: "Как-то, - вспоминал композитор - лауреат Сталинской 
  премии 1943 года, - ко мне подошел солдат в кирзовых сапогах - красивый, рослый 
  молодец, с румянцем во всю щеку, назвался Алексеем Фатьяновым, поэтом, прочитал, 
  встряхивая золотистой копной волос, свои песни. Песни мне понравились лиризмом, 
  напевностью, юмором. Так состоялось мое знакомство с поэтом, перешедшее затем 
  в содружество". Это содружество подарило России ее золотые песни, одну из них 
  - бессмертные "Соловьи" - маршал Жуков наряду со "Священной войной" и "Дорогами" 
  назвал одной из трех лучших военных песен.<br>
  <br>
  Первой творческой удачей Фатьянова и Соловьева-Седого стала песня "Нa солнечной 
  поляночке". Первоначально композитор написал ее в форме лирического вальса, 
  мечтательного и задумчивого. Приятная на слух музыка нравилась всем, кроме самого 
  гениального мелодиста, который чувствовал, что не передал той живости, удали, 
  теркинской неунываемости, которые были заложены в интонации стихов. Он решительно 
  отбросил старый вариант и извлек, гармонизировал уловленную интонацию. Песня 
  полетела по ансамблям, по ротам и батареям. Поэт-фронтовик Николай Старшинов, 
  с детства любивший песню, гармошку, частушку, не раз вспоминал, как августовским 
  утром 1943 года, после длительного марша по смоленским болотам, его батальон 
  вышел на опушку леса. "Мои товарищи от усталости едва не валились с ног. Тут 
  мы и услышали отчаянно веселую, зажигательную песню, а только потом увидели 
  наших разведчиков, лежащих среди некошеной густой травм в маскхалатах". Что 
  же пели эти люди, которые, может, только что рисковали жизнью?<br>
  <br>
  На солнечной поляночке, <br>
  Дугою выгнув бровь,<br>
  Парнишка на тальяночке <br>
  Играет про любовь...<br>
  <br>
  Какой простонародный и свежий оборот - "про любовь"!<br>
  <br>
  И так любая их песня - естественность дыхания, предельный лиризм, сочность языка, 
  свежесть и выразительность мелодии. Но главное - абсолютное следование и понимание 
  народной философии, певучей души.<br>
  <br>
  Но не только великие авторы внесли свой вклад в военную песню. Например, мало 
  кто знает, что стихи и музыку знаменитой песни "Тучи над городом встали", исполненной 
  молодым Марком Бернесом, написал второй режиссер фильма "Человек с ружьем" Павел 
  Арманд. В армии я был ротным запевалой, и, хоть служил в войсках связи, мы но 
  какой-то прихоти командования исполняли лирическо-строевую "Песню о Ладоге". 
  Оказывается, она была сложена самодеятельным автором, фронтовиком - капитаном 
  В. Богдановым.<br>
  <br>
  В одном из военных песенников напечатаны две песни, стихи и музыку которых написал 
  Л. Сергеев. Кто он? - не ведаю, но песня "Колоколенка" сложена в лучших народных 
  традициях, мастерски использует те интонации казацких и шутейных воинских песен, 
  которые использовал потом в лучших своих песнях Александр Розенбаум ("Есаул 
  молоденький").<br>
  <br>
  На горе, на горочке стоит колоколенка, <br>
  А с нее по полюшку лупит пулемет, <br>
  И лежит на полюшке сапогами к солнышку <br>
  С растакой-то матерью наш геройский взвод.<br>
  <br>
  Певица Татьяна Петрова упорно хотела включить в свой репертуар какую-нибудь 
  песню на стихи высоко ценимого ею Юрия Кузнецова, и, наконец, такое стихотворение 
  - "Кубанка", с рефреном и прозрачным развитием лирического сюжета в духе тех 
  же казацких песен - было опубликовано, положено на музыку и спето ею.<br>
  <br>
  В поэзии, особенно песенной, ясно видно, как спасительно и плодотворно возвращаться 
  к национальным истокам, к вековым заветам классической литературы. Эмигрант 
  Александр Герцен подвел черту под своими раздумьями: "Каждый русский сознает 
  себя частицей державы... Оттого-то, где бы русский ни жил на огромных пространствах 
  между Балтикой и Тихим океаном, он прислушивается, когда враги переходят русскую 
  границу, и готов идти на помощь Москве так, как шел в 1612 и 1812 годах". Мы 
  можем добавить - и в 1941-м тоже! И дело тут не только в том, что Москва - столица 
  державы, сердце Родины, но и в том, что здесь столкнулись все наречия русского 
  языка, зародились все жанры поэзии, авторской и массовой песни. Ведь недаром 
  существовало присловье: "В Москву - за песнями". Вот и эта антология выходит 
  в Москве, чтобы разойтись по России и напомнить о великой, запечатленной в лучших 
  строчках военно-патриотической традиции.<br>

