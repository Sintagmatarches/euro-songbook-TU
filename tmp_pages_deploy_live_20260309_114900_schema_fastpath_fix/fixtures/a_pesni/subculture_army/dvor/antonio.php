<br>
Песню написал студент Днепропетровского горного института Юрий Тарасьев в 1954 г. во время турпохода по Северному Уралу, прототипы героев - участники этого похода. Об этом сообщил публицисту Максиму Кравчинскому племянник Юрия Тарасьева Эрни Лайт, а затем сам Юрий рассказал историю создания и дал авторский текст. Эту же версию происхождения песни подтвердил сын Юрия Михаил Тарасьев в письме сайту a-pesni 19 марта 1922. <br>
<br>
Версия Юрия Тарасьева из книги Максима Кравчинский "Интеллигенция поет блатные песни" (Нижний Новгород: Деком, 2017. С. 225-227):<br>
<br>
<em>«Песня родилась в 1954 году во время
туристского похода высшей категории сложности по Северному
Уралу (сплошная тайга и бурная река Вишера, по которой мы сплавлялись на плоту). Нас было четверо. Все стали прототипами песни.<br>
    <br>
Виктор Вдовин – единственный опытный турист – командир
группы – “белоэмигрант” (родился в Финляндии – отец работал в русском посольстве), Толя Пурвинас – “Томми”, Алик Попов (“Сэр Анто-
ни”) – мой близкий друг и энтузиаст моего творчества, самозабвенно
пел со мной все мои песни, и я (“мне отец оставил миллионы…”) – все
студенты Днепропетровского горного института.<br>
    <br>
Условия, в которых проходил поход, были близки к обстановке,
описанной в песне. Мы действительно должны были соблюдать осторожность – во время похода из одного из лагерей бежали уголовники,
и наши документы могли бы очень им пригодиться.<br>
    <br>
    Остальное дорисовала наша фантазия, с учетом вполне актуальной в те времена темы заброски шпионов в нашу страну<sup>1</sup>. К моменту
окончания похода песня была уже полностью готова и исполнена
по возвращении в Днепропетровск. Пели под гитару. Она быстро завоевала популярность в нашем туристском кругу.<br>
    <br>
В нынешнем ее исполнении основной текст претерпел изменения: Сэр Антони превратился в Сэра Антонио, а имена героев меняются в зависимости от исполнителя, вместо иносказательного
“бэдный мой мать” часто используется прямолинейная нецензурщина, добавился куплет о гангстере “Сэр Антонио” (у меня этот
куплет не получался из-за того, что я не нашел подходящей рифмы
к фразе “как это по-русски…”), мелодия тоже несколько отличается от первоначальной. Одним словом, народное творчество над песней
    поработало, но главная идея осталась.<br>
    <br>
    Первоначальный авторский текст выглядел так:<br>
    <br>
    Ночь тиха, тайга шумит тревожно,<br>
    Нет ни шороха кругом.<br>
    По тропинке очень осторожно<br>
    Мы все четверо идем.<br>
    Мы идем по дорожке узкой,<br>
    КГБ вовек нас не поймать.<br>
    “Сэр Антони! Как это по-русски?<br>
    Бэдный мой мать!”<br>
    <br>
    Под ногами топкая трясина,<br>
    И уже, наверно, ищет наш десант.<br>
    И ведет нас русская скотина –<br>
    Виктор Вдовин – белоэмигрант.<br>
    Он подлец, он выдаст нас в кутузке,<br>
    Как бы не попасть нам с ним в беду!<br>
    “Сэр Антони! Как это по-русски?<br>
    How do you do!”<br>
    <br>
    Томми бросил ферму в Алабаме, записался в ФБР.<br>
    Он сказал good-bye старушке маме,<br>
    Я слетаю на минутку в ССР.<br>
    Не один посев испортит кукурузки,<br>
    Томми им покажет, как фермы разорять.<br>
    “Сэр Антони! Как это по-русски?<br>
    Бэдный мой мать!”<br>
    <br>
    Мне отец оставил миллионы,<br>
    Чтоб я мог свободно жить.<br>
    Спи, my father, мальчики-шпионы<br>
    Небоскреб сумеют защитить.<br>
    Низко гнусь под тяжестью нагрузки,<br>
    За тобой я медленно бреду.<br>
    “Сэр Антони! Как это по-русски?<br>
    How do you do!”<br>
    <br>
    Там вдали за синею горою<br>
    Наша мирная страна.<br>
    Мы в нее вернемся как герои,<br>
    Встретит нас объятьями она.<br>
    Рестораны, бары, вина и закуски,<br>
    Женщин нежных будем обнимать.<br>
    “Сэр Антони! Как это по-русски?<br>
    Бэдный мой мать!”<br>
<br>
    Слышен сзади громкий лай собачий –<br>
    По следам враги идут.<br>
    Сэр Антони – это крышка значит,<br>
    Это называется “капут”.<br>
    Ну так что ж! Покажем трясогузкам,<br>
    Как американцы могут умирать!<br>
    “Сэр Антони! Как это по-русски?<br>
    Бэдный мой мать!”…»<br>
    <br>
    <sup>1</sup> В основе песни лежат реальные события: первая заброска диверсантов произошла в конце апреля 1953 года на Украину. Операцию планировала английская разведка, а технически осуществляли
    американцы. Добровольцы нашлись из числа курсантов разведшколы ЦРУ под Мюнхеном. Все они
    были арестованы и через месяц расстреляны. <br>
    <br>
    Второй инцидент произошле в июне 1953 года. Восемь агентов НТС были сброшены на парашюте
    с американского самолета в районе Майкопа. Каждый имел при себе рацию, шифровальный блокнот и оружие. Все восемь энтээсовцев были схвачены сотрудниками госбезопасности. Провал произошел из-за информации, полученной советской разведкой от Кима Филби.
