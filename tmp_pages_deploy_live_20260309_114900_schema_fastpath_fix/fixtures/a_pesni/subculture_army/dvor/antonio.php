<span class='i'>Расшифровка фонограммы Константина Беляева (не ранее 2-й п. 1960х гг.) с форума <a href="https://tunnel.ru/post-dobrojj-vsem-nochi-byla-takaya-smeshnaya-pesenka-diversantov" target="_blank">"На завалинке"</a>, 14.01.12 (первая  фонограмма по порядку).<br></span>
