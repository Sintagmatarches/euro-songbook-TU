Как от самого белого моря<br>
Шел мужик не рыбак, не помор<br>
Шел острожник весь белый от горя<br>
А на лбу было вызжено Вор<br>
Шел до Вологды, шел торопился<br>
Чтоб утешить свою мать поскорей<br>
Да в болоте гнилом проволился<br>
Стал тонуть, Да Христа поминать<br>
За грехи я наказан острогом<br>
Нечто сгнию неувидевши мать<br>
И поклялся мужик перед Богом<br>
Коль спасет – век свой не воровать<br>
Затвердела трисина гнилая<br>
Кустик вырос под самой рукой<br>
И поклон небесам отбивая<br>
Заспешил сын острожный домой<br>
Долгий путь по лесам и болотам<br>
Одолел, шел к местам дорогим<br>
Умывался росою и потом<br>
И дошел небесами храним<br>
На хмельном Вологодском базаре<br>
Чтоб с пустыми руками не быть<br>
Стал острожник смотреть, что в подарок<br>
Своей матери старой купить<br>
Вот платки из парчи и сафьяна<br>
Шиты золотом, дороги. Ох!<br>
Мужику они не покорману,<br>
А украсть не простит кражу Бог<br>
Шел рядами мужик торговался<br>
Кто-то водочки чарку налил<br>
Во хмелю грешный сын не сдержался<br>
И платок залоченый украл<br>
Вот подходит он к дому родному<br>
На крылечке стоит его мать<br>
И по случаю оба такому<br>
Обнялись нежно, стали рыдать<br>
Ночь проплакали только на утро<br>
Сын платочек ворованый взял<br>
Материнские плечи окутал<br>
Тут Господь его и наказал<br>
Мать его покачнулась, упала<br>
Как березонька под топором<br>
На холодном полу умирала<br>
Не согрета сыновьем теплом<br>
Как обратно до синего моря<br>
Шел мужик не рыбак не помор<br>
В кондолах и весь белый от горя<br>
А на лбу было вызжено Вор.<br>
