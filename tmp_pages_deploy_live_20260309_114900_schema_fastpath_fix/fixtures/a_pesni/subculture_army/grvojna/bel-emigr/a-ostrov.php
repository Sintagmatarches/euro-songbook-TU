	<br><span class="text">  <strong>Сергей Осипов <br></strong>
  <br>
  <span class="header"><strong>ОСТРОВ КАЗАЧЬИХ КОСТЕЙ</strong><br></span>
  <br>
        <em>«Аргументы и факты» №49(1362), 6-12 декабря 2006 г.</em><br>
  <br>
  <br>
  <strong>85 лет назад, в декабре 1921 г., закончился исход казаков с греческого 
  острова Лемнос. Как они там оказались?</strong><br>
  <br>
  Во время эвакуации армии Врангеля из Крыма в 1920 г. Россию покинули примерно 
  150 тысяч человек. Обеспечивавшие отход англичане и французы разделили беженцев. 
  Армейские части высадили в Галлиполи (Турция), флот отправили в Бизерту (Тунис). 
  Казачьим частям достался греческий остров Лемнос, который со времён Первой мировой 
  войны контролировала Франция. Начиная с ноября там было размещено примерно 22 
  тысячи &quot;сабель&quot; - кубанские, донские и терско-астраханские казаки 
  с неучтённым количеством жён и детей. <br>
  <br>
        <strong>Слепая надежда</strong><br>
  <br>
  ЧТО первым делом строит русский человек на новом месте? Баню и церковь. И то 
  и другое казаки оборудовали в полученных от французской военной администрации 
  палатках. И начали жить. Сперва всё шло неплохо. Судя по тому, что при полевом 
  госпитале работало родильное отделение, казаки на чужбине &quot;плодились и 
  размножались&quot;. Донцы выпускали рукописный листок, а кубанцы - будто бы 
  даже набранную типографским способом газету. Давал представления театр под открытым 
  небом, работала читальня. Действовали даже мужской и женский пляжи. <br>
  <br>
  Сперва командирам удавалось поддерживать почти полное подобие жизни в воинской 
  части: подъём в 5 утра, гимнастические упражнения, строевая подготовка. Проводились 
  парады, работали офицерские курсы. Была надежда на то, что сегодня, завтра, 
  через месяц Антанта раздаст казакам оружие и пошлёт их отвоёвывать у &quot;красной 
  сволочи&quot; Кубань и Тихий Дон...<br>
  <br>
  Кубанцы с донцами жили порознь в лагерях, разделённых проливом. Судя по всему, 
  друг друга они недолюбливали. Французская же администрация особой разницы между 
  &quot;cosaque russe&quot; не видела. И тех и других из лагерей не выпускали 
  вооружённые патрули. И тех и других весьма скудно снабжали продовольствием. 
  На человека полкило хлеба, 300 г картошки, 200 г консервов, 30 г сахара и 4 
  г чая в день. <br>
  <br>
  Но если с едой ещё можно было как-то выкрутиться, сменять у греков сапоги или 
  ремни на барана (казаки называли Лемнос &quot;Бараньим островом&quot;), то с 
  дровами дело было совсем швах. Французские власти выдавали мало, а остров был 
  да и остаётся почти совершенно &quot;лысым&quot;. Дров не хватало даже на кипячение 
  питьевой воды. А это почти гарантированный брюшной тиф. Но эпидемия началась 
  не сразу, а только когда ослабла дисциплина. <br>
  <br>
  По гигиеническим соображениям в одном из лагерей вырыли отхожие места на пологом 
  склоне метрах в 100 ниже палаток. Казаки ругались, но ходили туда - пока держалась 
  дисциплина. Потом плюнули на приличия и стали бегать &quot;в кустики&quot; вверх 
  по склону. А потом пошли дожди... <br>
        <br>
  <strong>Неглубокие могилы</strong><br>
  <br>
  В середине 1921 г. стало окончательно ясно, что ни Англия, ни Франция с Советами 
  воевать не собираются. Казаки, недавние соратники в борьбе с большевизмом, стали 
  им не нужны. Лагеря на Лемносе было приказано закрыть к концу года. Большую 
  часть станичников удалось склонить к эмиграции в Болгарию и Сербию, около тысячи 
  завербовались во французский иностранный легион. Из тех, что вернулись в Россию, 
  треть была сразу же расстреляна в новороссийской ЧК.<br>
  <br>
  К концу декабря 1921 г. на Лемносе остался лишь один казак, женившийся на местной 
  женщине, и пять сотен казачьих могил. Между ними паслись тощие греческие овцы, 
  а кресты и надгробия из ракушечника растаскивали на хозяйственные нужды местные 
  жители. Так продолжалось до тех пор, пока 5 лет назад сотрудник российского 
  посольства в Болгарии Л. Решетников не наткнулся в тамошней библиотеке на подшивку 
  русского эмигрантского журнала &quot;Станица&quot; за 1923 г. Этой осенью российская 
  делегация во главе с вице-спикером Госдумы С. Бабуриным в третий раз посетила 
  Лемнос и провела панихиду над могилами предков.<br>
  <br>
  ...Примерно 6 соток на вершине пологого холма обнесены проволочным забором. 
  Над воротами надпись: &quot;Русское казачье кладбище 1920-1921&quot;. Посредине 
  скромный беломраморный крест, поставленный в прошлом году, и два десятка могил. 
  Вернее, то, что от них осталось. Надписи на плитах стёрлись.<br>
  <br>
  Тут же большая братская могила умерших от тифа - в ней лежат останки примерно 
  240 человек. Захоронение неглубокое, уж больно почва на Лемносе твёрдая. Собственно, 
  могилы сохранились только потому, что эта земля не имеет никакого сельскохозяйственного 
  значения. Сколько ни удобряй русскими костями, из неё всё равно растут только 
  камни. <br>
  <br>
  <em>о. Лемнос - Москва </em><br>
