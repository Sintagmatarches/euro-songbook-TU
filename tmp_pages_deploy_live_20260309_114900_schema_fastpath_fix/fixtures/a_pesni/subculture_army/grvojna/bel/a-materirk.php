  на пароход «Батарею». По выходе из Владивостока пароходы зашли в залив «Посьет», 
  там по приказанию ген[ерала] Глебова все конные высадились для присоединения 
  к 3-му Корпусу ген. Молчанова. От нашей дружины отделился конный взвод под командой 
  есаула Дзеражинскаго, с прикомандированием к сотне Атамановского полка, а сотня 
  присоединилась с конными Забайкальцами других полков, амурцами и артиллеристами 
  Заб[айкальской] Дивизии и образовали Казачий Дивизион под ком[андой] Войскового 
  старшины Фильшина. <br>
  <br>
  1-го ноября 1922 г. мы перешли Китайскую грани- цу в районе Хунчуна и, сдав 
  оружие и пройдя с остановками через гор. Хунчун, Янзыган, Омасо, прибыли в гор. 
  Гирин, конечный пункт, где наших лошадей отобрали в казну за минимальное денежное 
  вознаграждение, которое было выдано казакам на руки. <br>
  <br>
  На этом заканчивается существование строевых частей. Иркутские казаки вместе 
  с Забайкальцами попадают в концентрационный лагерь гор. Лувансян, находящийся 
  в 80 верстах от Чань-чуня и в 60 вер. от ст. Яомынь К.В.Ж.Д. Из города Луван-сяна 
  казаки с большим трудом убегая, пробираются одиночками и небольшими партиями 
  в г. Чань-чунь и на линию К.В.Ж.Д., где и оседают, приискав себе ту или иную 
  работу. <br>
  <br>
  Иркутская Дружина, прибывшая на пароходе с группой ген[ерала] Глебова в Корею 
  (порт Гензан), попадает вместе с Атамановским полком, с которым тесно сплетается 
  вся служба за описанный период, в один район на земляные работы по прорытию 
  оросительных каналов и этим тяжелым трудом зарабатывает свое пропитание. <br>
  <br>
  По истечении некоторого времени, группа ген[ерала] Глебова на тех же кораблях 
  и транспортах отправляется в Шанхай, и вот... <br>
  <br>
  1-го марта 1924 г. казаки Иркутяне, находящиеся на транспорте «Охотск» на рейде 
  Вузунг, лишаются своего доблестнейшего, любимого командира Войскового Старшины 
  Максима Степановича Петрова, убитого предательской пулей возвращенцев под водительством 
  б[ывшего] генерала предателя Анисимова, взбунтовавшего казаков и на транспорте 
  «Монгугай», уведшего их в гор. Владивосток к красным. <br>
  <br>
  ПОТЕРЯН ЛЮБИМЫЙ НАЧАЛЬНИК! <br>
  <br>
  Память о нашем последнем командире - Войсковом Старшине Петрове будет вечно 
  жить в сердцах казаков-иркутян. <br>
  <br>
  <em>Луч Азии. 1936. Апрель. №20/4. С. 17-22. </em><br>
  <br>
        <br>
        <strong>Л. Гофман. Из боевых эпизодов на Канском фронте</strong><br>
  <br>
  Прежде чем приступить к описанию боев под деревнями Тополь и Улюколь на Канском 
  фронте, необходимо сказать, что официально такого фронта не существовало, т.к. 
  по донесению генерала Розанова, фронт этот был полностью ликвидирован еще летом 
  1919 года, в действительности же дело обстояло совсем не так. Фронт не только 
  существовал, но и представлял из себя постоянную угрозу. С нашей стороны, на 
  фронте находились 55[-ый] Сиб[ирский] Стрелковый полк, подкрепленный впоследствии 
  56[-ым] и 32[-ым] стрелковыми полками, каждый приблизительно численностью в 
  500 штыков. К отряду был придан один взвод артиллерии и 2 сотни Иркутских казаков. 
  Боевое значение пехотных частей было невысокое, так как личный состав состоял 
  по преимуществу из молодых солдат, только что прибывших на пополнение, не получивших 
  достаточной воинской подготовки; кроме того, в части поступали уже распропагандированные 
  солдаты, которые при первом удобном случае стали уходить к красным, вначале 
  по одиночке, а потом целыми группами. Вся тяжесть борьбы с хорошо вооруженными 
  повстанцами легла на плечи крестьянских дружин и двух сотен иркутских казаков. 
  Примером такого положения могут, хотя бы служить описываемые ниже боевые действия 
  за обладание деревнями Тополь и Улюколь. <br>
  <br>
  В конце октября 1919 г. в связи с крушением Западного фронта и в целях предупреждения 
  возможности возникновения активности красных и попыток их к перерыву связи по 
  железной дороге, Командующим Енисейским округом был отдан приказ о занятии деревни 
  Тополь, которая служила одной из основных баз красных. Для производства боевых 
  операций был сформирован особый отряд из частей 32[-го] и 55[-го] стрелковых 
  полков, крестьянских дружин, двух сотен иркутских казаков и взвода артиллерии. 
  Вся численность отряда определялась и 700 человек. Отрядом командовал полковник 
  32[-го] полка Есипов. Отряд был разбит на три колонны, причем в левой колонне 
  находился один взвод казаков, в правой — полсотни и в средней — сотня. <br>
  <br>
  Деревня Тополь была хорошо укреплена красными. Были сделаны сплошные окопы с 
  бойницами. <br>
  <br>
  Первой к деревне подошла в 6 часов утра левая колонна, пехота залегла у огородов. 
  Посланные для связи донесли, что никаких признаков движения ни средней, ни правой 
  колонн не обнаружено. Наше присутствие (левой колонны) было обнаружено. Поднялся 
  сильный лай собак, красные подняли тревогу и в результате по нашему отряду был 
  открыт сильный ружейный огонь со стороны красных. Видна было, как из деревни 
  по разным направлениям поскакали конные, видимо, с целью предупреждения соседних 
  районов, а может быть и с целью разведки, какие именно из дорог заняты белыми. 
  Стрелки, находившиеся в цепи, страдали от холода. Люди мерзли. Нужно было предпринять 
  какое-то решение, но таковое командующим отрядом не предпринималось, что заставило 
  командира казачьего взвода Юдина и начальника крестьянской дружины М.М. Желякевича 
  на собственный риск предпринять атаку красных. Последние, не ожидая стремительности, 
  быстро очистили свои окопы, в результате чего казаками и дружинниками были заняты 
  окопы. Однако удержаться в окопах противника долго не удалось, т.к., получив 
  подкрепления, красные повели энергичное наступление на занявших окопы казаков. 
  Не получая поддержки от своей пехоты и не имея сведений о положении дела в других 
  частях отряда, окопы пришлось оставить. Среди казаков были тяжело раненые. В 
  течение дня была сделана еще одна попытка выбить красных из деревни. Благодаря 
  атаке 2[-ой] сотни казаков под командой есаула С. Коршунова и энергичному наступлению 
  взвода казаков под командой Юдина и дружинников под командой Желякевича, была 
  занята окраина деревни и в самой деревне шел бой на улицах. Сосредоточив на 
  2[-ой] сотне есаула Коршунова сильный пулеметный и ружейный огонь, противник 
  приостановил дальнейшее их продвижение. Дальнейшее движение вперед не получило 
  развития еще и потому, что пехотные части и в данном случае отчасти, благодаря 
  нерешительности начальника отряда, отчасти вследствие плохого управления ими, 
  никакой поддержки не оказали. Подошел вечер, и начальник отряда командир полка 
  полковник Есипов отдал приказ об общем отступлении. Как только наметилось наше 
  отступление, красные атаковали левую колонну. Только находчивость и отвага казаков 
  и дружинников предупредила опасность. Казаки бросились в атаку, смели передние 
  ряды атакующих и отбросили их с большими потерями, после чего они уже больше 
  не делали попыток к преспедованию отступающих. В этом бою был ранен командир 
  сотни есаул Коршунов и убит вахмистр Брюханов. <br>
  <br>
  За неудачное ведение операций под Тополем впоследствии был смещен полковник 
  Есипов, и начальником отряда был назначен командир Иркутского Казачьего полка 
  полковник Бычков. Фронт был усилен еще одной сотней иркутских казаков. <br>
  <br>
  Вновь назначенный начальник отряда полковник Бычков, хотя и был человеком в 
  высшей степени волевым и решительным, но эти качества для фронта никакой роли 
  сыграть уже не могли. Изменилась обстановка не только на самом Канском фронте, 
  но главным образом — на Западном фронте. Безостановочное и беспорядочное отступление 
  на Западном фронте предопределило судьбу и Канского фронта. В середине декабря, 
  несмотря на то, что вследствие явных признаков разложения пехотных частей, была 
  ясна невозможность ведения каких-либо боевых действий, все же для ликвидации 
  красных, скопившихся вблизи линии железной дороги, приказано было занять деревню 
  Улюколь. Посланный для занятия Улюколя отряд состоял из 56[-го] и 2 рот 32[-го] 
  полков и двух сотен казаков. Узнав о движении отряда, красные партизаны сосредоточили 
  в месте боя отряд численностью в 1000 конных и пеших бойцов из наиболее надежных. 
  В самом начале боя казаки наткнулись на сильно укрепленные позиции красных в 
  лесу. Завязался жестокий бой. Позиции переходили два раза из рук в руки. Красные 
  в этом бою потеряли двух своих наиболее талантливых командиров Красных партизан 
  Кокоулина и Орлова. Среди казаков было много раненых, среди них командир пулеметной 
  команды есаул Петров. В этом бою особенно отличились: помощник начальника пулеметной 
  команды Панчехин и казаки братья Федоровы, которые своей находчивостью и смелостью 
  в условиях лесного боя не раз с честью спасали положение. Несмотря на лихую 
  работу казаков, в конечном результате полковнику Бычкову пришлось отдать приказ 
  об отступлении, т.к. со смертью командира батальона штабс-капитана Чернышева 
  — храброго и хорошего офицера пехотные части не только не стали оказывать никакого 
  содействия, но роты 56[-го] полка, посланные в обход, стали целыми частями переходить 
  на сторону красных.<br>
  <br>
  Описывая боевые эпизоды Канского фронта, нельзя обойти молчанием крестьянских 
  дружин, действовавших в этом районе. Крестьянские дружины в большинстве случаев, 
  благодаря знанию местности, шли в авангарде и принимали на себя первые удары. 
  Все дружины работали во имя идеи. Прекрасные наездники, хорошо владеющие оружием 
  на коне, для красных они всегда являлись грозой. <br>
  <br>
  Из всех начальников крестьянских дружин случайно остался живым, ныне находящийся 
  в Харбине, М.М. Желякевич, участник многих боев на Канском фронте, в том числе 
  и описываемого выше боя под деревней Тополь. <br>
  <br>
  В общем, рассматривая бои под деревнями Тополь и Улюколь, надо отдать справедливость, 
  что действия иркутских казаков, как в одном, так и в другом случае носили лихой, 
  твердый и определенный характер, и если бы такую поддержку они встретили бы 
  со стороны пехоты, то результаты, несомненно, были бы другие. Благодаря малочисленности 
  казаков, одной смелости, храбрости и настойчивости оказалось мало. <br>
  <br>
  Это были последние бои на Канском фронте, т.к. вслед за этим, в силу развала 
  и отступления на Западном фронте, последовал автоматический переход фронта в 
  руки красных. <br>
