	<br><span class="text"><span class="header"><strong>МАТЕРИАЛЫ 
  К ИСТОРИИ АНТИБОЛЬШЕВИЦКОГО ДВИЖЕНИЯ В ИРКУТСКОМ КАЗАЧЬЕМ ВОЙСКЕ</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 269-277</em>.<br>
  <br>
  <br>
  <em>Часть публикуемых ниже уникальных материалов по истории участия иркутских 
  казаков в Гражданской войне была напечатана в альманахе «Иркутский казак» (Харбин, 
  1934), единственный известный экземпляр которого хранится в библиотеке им. Гамильтона 
  Гавайского университета в Гонолулу. Копии материалов Иркутского казака» были 
  любезно предоставлены редакции нашего альманаха русским библиографом Патрицией 
  Полански. Некоторые из публикуемых ниже документов были переданы нам атаманом 
  Иркутского казачьего войска (общественная организация) Н.М. Мериновым. <br>
  <br>
  Редакция </em><br>
  <br>
        <br>
  <strong>Доклад Войскового атамана Иркутского казачьего войска П.П. Оглоблина 
  Верховному Правителю адмиралу А.В. Колчаку об утверждении Иркутского казачьего 
  войска. 20.05.1919 г. <br>
  </strong><br>
  Войсковой атаман Иркутского казачьего войска <br>
  Его Высокопревосходительству Господину Верховному Правителю <br>
  <br>
  20 мая 1919 года <br>
  №14 <br>
  г. Омск <br>
  <br>
  Доклад об утверждении Иркутского казачьего войска <br>
  <br>
  Предки казаков Иркутского казачьего войска, завоевывая Сибирь, в частности территорию 
  Иркутской губернии, в 1652 году заложили ясачное зимовье в устье реки Иркута 
  (ныне город Иркутск). <br>
  <br>
  Иркутское ясачное зимовье обселилось сходцами промышленников и проворных людей. 
  По важности в то время положения среди бурят в 1661 году это зимовье превратилось 
  уже в укрепленное место и возведено в степень острога. <br>
  <br>
  В 1662 году в заимке казаками была выстроена деревянная церковь во имя св. Спаса; 
  знамя с изображением св. Спаса, с которым казаки шли, хранится при войске, как 
  реликвия. <br>
  <br>
  В 1666 году было дано распоряжение об укреплении Иркутского острога, - как опора 
  к Байкалу. <br>
  <br>
  В 1676 году для обороны Иркутского острога, вверх по реке Иркуту, в местности 
  Тунка, было построено Тункинское зимовье, а потом острог. <br>
  <br>
  В 1686 году, вследствие грамоты от 21 апреля, Иркутский острог получил действительно 
  имя острога с правом управления округом за счет уездов: Енисейского, Илимского 
  и с включением острогов: Балаганского, Идинского, Верхоленского и Бельского. 
  <br>
  <br>
  В 1690 году Иркутский острог был обращен в степень города (исторический обзор 
  Сибири, за 1838 год, страница 76, 78, 214). <br>
  <br>
  В 1692 году отряд Иркутских казаков был отправлен в город Нерчинск для усмирения 
  монголов (рукопись отд. Академии Наук, стар. стран. арх. 117). В 1687—1688 годах 
  Иркутские казаки представили послу Головину, следующему в Забайкалье, суда и 
  способы к перевозке артиллерии, пороховой казны и трех полков. <br>
  <br>
  В 1697 году в Иркутске был установлен новый штат казаков: конных 1150 и, кроме 
  того, 100 человек прежних пеших казаков были переведены в конные (Р.С.А. Наук 
  списки Ирк. арх. стр. 129). <br>
  <br>
  Одновременно с сим часть Иркутских казаков была отправлена для укомплектования 
  Селенгинских казаков. <br>
  <br>
  В 1716 году отрядом Иркутских казаков, под командой полковника Елчина были произведены 
  морские экспедиции на морские острова, «которые видны с Якутского берега». (Б.А. 
  списки Иркутского арх. стр. 161).<br>
  <br>
  В 1726 году часть Иркутских казаков была переведена и приписана к Нерчинским 
  казакам. <br>
  <br>
  В 1737 году, по распоряжению Сибирского приказа, от 7 октября 1737 года, в Иркутской 
  провинции положено: в городе Иркутске казаков конных и пеших 219 человек (стр. 
  арх. 66, 1080). <br>
  <br>
  В 1747 году были командированы Иркутские казаки на Камчатку для усмирения бунта 
  коряков. <br>
  <br>
  В 1752 году бригадиром Якобием предпринимается ряд реформ для организации пограничной 
  стражи и для комплектования этой стражи были посланы Иркутские казаки (стр. 
  арх. 1230, стр. 21). <br>
  <br>
  В 1753 году вновь было приказано командировать Иркутских казаков на Камчатку. 
  <br>
  <br>
  В 1755 году значительная часть Иркутских казаков вновь отправлена для защиты 
  и охраны Селенгинской границы (арх. XXVI рукоп. 14). <br>
  <br>
  В 1756 году было обращено особое внимание на оборону Забайкальского края, и 
  значительная часть Иркутских казаков вновь была послана для охраны границ (стр. 
  арх. 1551). <br>
  <br>
  В 1758 году по проекту губернатора Соймонова 400 семей казаков, исключительно 
  Иркутской провинции, были переселены в Забайкалье для охраны Акщинской границы 
  (стр. арх. 577, 636). <br>
  <br>
  В 1760 году русско-китайские дела обострились настолько, что вновь потребовалось 
  укрепление границ, и 400 казаков — Иркутских и Енисейских — вновь были посланы 
  в различные места Сибири (арх. №1527, стр. 477, 636, 641, 267, 351). <br>
  <br>
  В 1764 году, по утверждении положения об устройстве пограничного войска на китайской 
  границе, часть Иркутских казаков вновь была переселена в различные места Сибири 
  и Забайкалья (стр. арх. №486, стр. 225). <br>
  <br>
  В 1172 году 15 ноября генерал-майор Бриль сделал распределение казаков Иркутской 
  губернии для службы на китайской границе, и, таким образом, часть Иркутских 
  казаков была переселена с женами и детьми уже навсегда в пограничных сторожках-караулах. 
  <br>
  <br>
  В 1796 году Правительство освободило казаков от подушной подати, не привлекая 
  их к службе (apx. канц. В.М. №1804, стр. 87, 226). <br>
  <br>
  22 января 1797 года саамам коллегия возбудила вопрос о новом устройстве пограничного 
  войска в Иркутской губернии и необходимости преобразования вообще войск. <br>
  <br>
  О жалком состоянии казаков Иркутской губернии комендант Г.М. Блюм донес рапортом 
  от 14 июли 1798 года в Военную Коллегию (арх. МИД, дело Головкина, 1805 г., 
  стр. 189 приложения). <br>
  <br>
  24 мая 1798 года Военная коллегия известила указом Иркутского Военного Губернатора 
  об учреждении в Иркутске пятисотенного казачьего полка. В это время в Иркутской 
  казачьей команде состояло налицо: 4 сотника, 8 урядников, 16 капралов, 379 казаков, 
  а всего 407 человек. Хотя этого количества нижних чинов до полного комплекта, 
  510 человек, не хватало, но, тем не менее, Иркутский казачий полк был сформирован 
  (стр. архива В.М., стр. 2262, №87). <br>
  <br>
  В 1798 году казаки Иркутской губернии (нынешние Заб.) переименованы в гражданское 
  ведомство и были подчинены гражданскому губернатору (арх. В.М. 1804, стр. 230). 
  <br>
  <br>
  25 февраля 1801 года Военная коллегия передала и суд над казаками вообще в руки 
  гражданского начальства. <br>
  <br>
  В 1802 году Иркутский губернатор Лецанно подписал Иркутской казенной палате 
  отвести для пограничных казаков на каждую душу мужского пола под усадьбу, покосы 
  и пашни по 6 десятин, в силу Сенатского указа от 27 марта 1793 года. <br>
  <br>
  Для отвода земель в 1804 году был командирован землемер Ребров, но даже для 
  такого скромного распределения и наделения землею казаков, встретились препятствия 
  в недостаче удобной земли, и казаки вновь остались без всего (арх. М.И.Д., дело 
  Головкина, карт. 8, стр. 196). <br>
  <br>
  Таким образом, предки Иркутских казаков, завоевывая для своей Родины земли, 
  оберегали их неустанно от врагов, несли службу как городовые и пограничные казаки 
  и управлялись в составе полков полковыми атаманами. <br>
  <br>
  Это в 1822 году было Правительством закреплено особым уставом, как особая их 
  организация. <br>
  <br>
  Затем Правительство изданием положения 1851 года разрушило эту организацию, 
  образовав один полк, и подчинило казаков гражданскому управлению. <br>
  <br>
  В 1871 году Правительство расформировало полк, оставив одну отдельную сотню, 
  причем полковое знамя св. Спаса хранится при Войсковом Правлении, а полковая 
  икона в честь Александра Невского, сооруженная в 1829 году, передана в Успенскую 
  церковь в городе Иркутске, где и хранится до сего времени. <br>
  <br>
  Казаков, переведенных из других сословий в казаки, перевело в крестьянство и 
  лишило их законоположения, изданного для всех казачьих войск, помещенного во 
  2-ом томе Св. Зак. Рос. Имп., и оставшееся коренное население казаков стало 
  жить по положению, указанному в 10-ом томе Св. Воен. Постан. для Иркутских и 
  Енисейских казаков, не отвечающему требованиям нормальной жизни казаков. <br>
  <br>
  Это ненормальное положение еще в 80-х годах обратило внимание Правительства, 
  и был поднят вопрос о распространении на казаков Иркутского войска общего положения 
  о казаках. Вследствие чего, возбужденный в 1880 году вопрос бывшим Иркутским 
  генерал-губернатором генерал-адъютантом графом Игнатьевым и впоследствии поддержанный 
  его преемником генералом от инфантерии Горемыкиным, вопрос реорганизации на 
  новых началах казачьего населения Иркутской и Енисейской губернии путем распространения 
  на него давно уже действующих во всех казачьих войсках положений о военной службе 
  и воинской повинности, а также об общественном управлении, был выдвинут в 1898 
  году на очередь. <br>
  <br>
  Военное Министерство, разделяя мнение главных начальников края о необходимости 
  переустройства коренных Иркутских и Енисейских казаков, высказалось за образование 
  из них особого небольшого войска. <br>
  <br>
  Вслед за тем, после поездки Военного Министра в Сибирь и после доклада Государю 
  о необходимости реорганизации Иркутских казаков, был специально командирован 
  в г. Иркутск от Правительства по распоряжению Военного Министра с Высочайшего 
  повеления в 1900 году, Войсковой старшина Путинцев, который и представил Военному 
  Министру обширный материал с определенными выводами о невозможности оставлять 
  казаков Иркутского войска в прежнем состоянии, и тогда уже предполагалось распространить 
  на них общее положение о казаках. Но вопрос этот остался неразрешенным вследствие 
  войны и последующих событий. <br>
  <br>
  В докладе Войскового старшины Путинцева о положении Иркутского войска, рукою 
  Военного Министра были сделаны пометки следующего содержания: 1. В разделе о 
  расформировании полка в 1871 году и передаче казаков в ведение гражданского 
  начальства сделана пометка: «Ошибка»; 2. В разделе о военной службе и воинской 
  повинности, по положению 1871 года, пометка: «Весьма невыгодные условия», 3. 
  В разделе о тяжелых повинностях и налогах, относимых казаками Иркутской и Енисейской 
  губерний, пометка: «Надо оставить казаков», 4. В разделе о землепользовании 
  казаков пометка: «Весьма печально» - и вообще другие пометки, указывающие на 
  необходимость реорганизации Иркутского казачьего войска. В данное время, как 
  и в прежнее, казаки Иркутского войска, жертвуя для спасения родины и своей земли 
  и не зная в своих рядах большевиков, ярко доказали свое право быть казаками 
  общей казачьей, что уже фактически и проведено жизнью последующих годов. <br>
  <br>
  Но до сих пор, несмотря на то, что казаки Иркутского войска, несли и несут службу 
  наравне с другими казачьими войсками, участвуют в войне с Китаем, Японией, Германией 
  и в данное время борются на фронте с большевиками и несут службу по охране тыла 
  и Иркутской губернии, нет государственного акта, санкционированного Верховной 
  властью, о включении Иркутского казачьего войска, как отдельного, в общую казачью 
  семью, что не дает той моральной поддержки, каковая нужна в данный момент. <br>
  <br>
  Совещание представителей казачьих войск, при помощнике Военного Министра, по 
  делам казачьих войск, в заседании своем от 23 декабря 1918 года за №8, рассмотрев 
  вопрос о признании Иркутских казаков отдельным войском и принимая во внимание 
  невозможность оставления Иркутских казаков в прежнем положении, постановило 
  ходатайствовать перед Правительством о следующем: отменить стат. 5 учрежден. 
  Гражд. Управ. казаков (том 2, Св. Зак. Рос. Имп.) и признать Иркутское и Енисейское 
  казачье войско, наравне с прочими казачьими войсками — Иркутским и Енисейским 
  казачьими войсками, с предоставлением им права временного управления, ныне фактически 
  существующими органами Войскового Самоуправления, впредь до издания нового положения 
  о казаках Российских и Сибирских войск (приложение к протоколу №8 от 23 декабря 
  1918 г.). <br>
  <br>
  Докладывая о вышеизложенном Вашему Высокопревосходительству, испрашиваю Вашего 
  повеления об утверждении казаков Иркутского казачьего войска отдельным войском, 
  входящим в состав Великого Российского казачества и на благо родины и созидания 
  единого, крепкого и могущественного Государства Российского. <br>
  <br>
  Подлинное подписал: <br>
  Войсковой атаман Иркутского казачьего войска генерал-майор Оглоблин <br>
  <br>
  С подлинным верно: <br>
  И.д. адъютанта прапорщик (подпись) <br>
        <br>
  <br>
        <strong>Штаб-трубач Синанов. Иркутские казаки в гражданской войне</strong><br>
  <br>
  Написав настоящее воспоминание кратко и сжато, я не описываю многих боевых эпизодов 
  и боев, а также подвигов героев казаков. Этим воспоминанием я хочу только воскресить 
  в памяти события, участниками которых были Иркутске казаки: чтоб каждый казак 
  мог легче вспомнить то, что не должно забываться.<br>
  <br>
  О борьбе против большевиков казаков Иркутского Казачьего войска в гражданскую 
  войну, (события [19]17 и [19]19 г.) хорошо известно всем Сибирякам, а также 
  участникам Сибирской Армии Адмирала Колчака. После освобождения гор. Иркутска 
  в июле 1918 г. от большевиков, Добровольческий казачий отряд Войскового Старшины 
  А.А. Могилева, развертывается в дивизион, а затем в 4-х сотенный полк при учебной 
  и пулеметной команде. Полк принимает офицер Л[ейб]-Гв[ардии] Сводно-казачьего 
  полка забайкалец полковник П.И. Войлошников. <br>
  <br>
  В 1919 году полк посещает походный Атаман всех казачьих войск Ген[ерал]-лейт[енант] 
  А.И. Дутов и назначает командиром полка прибывшего с ним полковника А.Г. Бычкова 
  (Оренбургского войска). <br>
  <br>
  При отъезде Атамана Дутова в гор. Омск, откомандировывается в распоряжение Атамана, 
  учебная команда полка под командой Сотника С. Немчинова. <br>
  <br>
  Отправляется на внутренний фронт Енисейской губ[ернии] 3-я сотня под командой 
  Есаула Скуратова, а остаток полка с командиром полковником Бычковым выступает 
  на Северо-Канский фронт, где и участвует в непрерывных экспедициях по ликвидации 
  крупных отрядов партизан Кравченко, Щетинкина, Нижегородова, Лобова и др., и 
  в боях у села Веришко-Рыбинское, у сел Топол, удер. Уликоль и др. <br>
  <br>
  События в конце 1919 г. (захват власти политическим центром) застают полк стянутым 
  в гор. Канск за исключением 3-й сотни, находящейся на ст. Клювина, прикомандированной 
  к 2-му Чешскому кав[алерийскому] полку и кадрового остатка полка в гор. Иркутске, 
  где под начальством Войскового Атамана Ген[ерал]-лейт[енанта] П.П. Оглоблина 
  он стойко боролся с выступившими большевиками за обладание властью в городе. 
  <br>
  <br>
  Одновременно с событиями в Иркутске, находящиеся в г. Канске сотни удерживают 
  город в своих руках в ожидании подхода отступающей Армии Ген.- Каппеля и 3-й 
  сотни полка с фронта. <br>
  <br>
  Казаки держат город продолжительное время, к ним присоединилось небольшое количество 
  партизан Микрушенской и Шеламовской конных дружин со своими начальниками М.М. 
  Желякевич и Чунавиным. Эти начальники дружин были находчивы, стойки и храбры 
  в бою, их любили партизаны и казаки. Дружинники были из местных крестьян, отличные 
  стрелки и незаменимы при операциях против красных. <br>
  <br>
  К полку также примкнула часть офицеров и пехотных полков, так как весь гарнизон 
  в составе нескольких Сибирских пехотных полков и 3-х батарей перешел на сторону 
  красных партизан и повстанцев, окруживших город кольцом. <br>
  <br>
  Красное командование предъявило полку ультиматум - сдать оружие, грозя в противном 
  случай поголовным уничтожением всех казаков. <br>
  <br>
  Сначала Командир полка полковник Бычков категорически отказался подчиниться 
  этому требованию. Но пробиться в сторону Иркутска не представлялось возможным, 
  так как чехи, охранявшие линию ж[елезной] д[ороги], отказались пропустить иркутян 
  под жел[езно]дор[ожным] мостом. Идти навстречу Сибирской Армии тоже было нельзя, 
  так как не было точных сведений о ее продвижении. <br>
  <br>
  Поэтому командиру полка полк[овнику] Бычкову пришлось, скрепя сердце, согласиться 
  на сдачу оружия, тем более, что всякая надежда на выручку и поддержку с чьей 
  бы то ни было стороны была окончательно утеряна. <br>
  <br>
  До сдачи оружия казаки предоставили возможность скрыться в Чешские и Румынские 
  эшелоны тем, кому красные грозили суровой расправой. Команд[иру] полка Полковнику 
  Бычкову, Есаулу Фереферову, Хорунжим Стуканову, Смирнову, Переломову, Подхорунж[ему] 
  Панчехину, Начальнику Дружины Желякевич и нескольким казакам. После сдачи оружия 
  казаки двумя специальными эшелонами были отправлены в гор. Иркутск. По пути 
  следования эшелона с казаками, несмотря на заверения чехов, многие офицеры и 
  казаки были сняты и расстреляны или отправлены на принудительные работы в районе 
  гор. Нижнеудинска. <br>
  <br>
  По требованию, предъявленному Войсковому Правительству Политическим Центром, 
  прибывшие в Иркутск с фронта казаки были мобилизованы и сформирован полк. Через 
  некоторое время с Енисейского фронта с Чехами пришла 3-я сотня под командой 
  сотн[ика] Устинова и была влита в состав полка. <br>
  <br>
  В феврале 1920 г. полк выступил в составе особой Дальневосточной группы Калашникова 
  на фронт против находящихся в Забайкалье войск Атамана Семенова и отступившей 
  Армии Ген[ерала] Каппеля. <br>
  <br>
  Так был сформирован «Иркутский Советский каз[ачий] полк», состоявший из непримиримых 
  врагов большевизма, казаков знавших друг друга в боях против красных и явно 
  выражавших свою антипатию и враждебность к новой власти. Красные военкомы чувствовали 
  это и знали, но принять активные - решительные меры не могли, так как боялись 
  единственной регулярной части из казаков-колчаковцев, как нас называли в красной 
  армии, с командным составом казаков-офицеров, спаянных единым сильным казачьим 
  духом. <br>
  <br>
  Были попытки высшего командования исправить свою ошибку, разоружить казаков 
  и расформировать по частям Армии, но «казак не дремлет». Будучи окружены ночью 
  в Верхнеудинске Зимским партизанским полком, предъявившим требование разоружиться, 
  казаки оказались готовыми дать отпор и партизаны ушли ни с чем. <br>
  <br>
  Летом 1920 г. полк был переброшен на Ингодинский фронт (по реке Ингоде), где 
  со стороны Армии Атамана Семенова находился 1-й Конный Атамана Семенова полк 
  и другие части. <br>
  <br>
  Казаки быстро установили связь с Атамановцами, соприкасаясь заставами и разведкой. 
  Было выяснено, что помощником Командира Атамановского полка является пользующейся 
  авторитетом у казаков полковник Бычков - бывший наш командир на Северо-Канском 
  фронте, знающий каждого из казаков s лицо и кто к чему пригоден. С этого времени 
  веселей и бодрей стали казаки, они верили, что старый командир, находясь в противоположном 
  лагере, сумеет выручить своих казаков из красного плена. <br>
  <br>
  Не вдаваясь в подробности нашей конспиративной работы по подготовке перехода 
  полка на сторону войск Атамана Семенова, скажу, что план был разработан быстро 
  и наступил желанный день сбросить маску и стать как раньше в ряды единомышленников 
  для продолжения борьбы за освобождение Родины. <br>
  <br>
  Это был Духов день 1920 года, полк находился в деревне Бальзой. Насколько удачно 
  проводилась до тех пор конспиративная работа по подготовке перехода настолько 
  при осуществлении плана в решительный момент встретилось много затруднений. 
  Военному комиссару группы стало известно о готовящемся переходе полка. <br>
  <br>
  При появлении наступающего противника (дивизиона Атамановского полка с конной 
  дружиной) наш полк был намеренно поставлен на противоположный фланг тому, который 
  был предназначен раньше. Очутившись под интенсивным обстрелом со стороны конной 
  Дружины казаков-партизан, командиры сотен были озадачены и, неся потери убитыми 
  и ранеными и не имея возможности применить оружие е виду неясности обстановки, 
  стали произвольно отводить сотни с фронта, этим была нарушена связь в полку. 
  <br>
  <br>
  Точно такое же положение было и на участке Пулеметной команды. Находясь под 
  обстрелом казаки были в недоумении; но находчивость неустрашимого начальника 
  пулеметной команды Подхорунжего Панчехина вывела пулеметчиков из критического 
  положения. Подхор[унжий] Панчехин бросается под огнем вперед, махая фуражкой. 
  Дружинники, пораженные, прекращают стрельбу и вступают в переговоры. Тут же 
  выясняется недоразумение. Оказывается Дружина получила распоряжение на своем 
  фланге вести наступление в боевом порядке и интенсивно поддерживать огонь, чтобы 
  ошеломить своим натиском красноармейцев и предоставить возможность на другом 
  фланге совершить nepexoд Иркутскому полку, где находились Атамановцы с полковн[иком] 
  Бычковым. Выяснив недоразумение, Пулеметная команда, получив лошадей от коноводов, 
  в конном строю с криком «Ура!» присоединилась к дружине. <br>
  <br>
  Привожу положение 2-й сотни. Сотня находится под обстрелом! Командиру доносят, 
  что сотня уходит с[о] своего участка. На участке Пулеметной команды идет бой, 
  но будто бы есть приказ отходить и Пулемет[ная] к[оман]да тоже отойдет. Находясь 
  в неведении всего происходящего, командир не решается единолично решать судьбу 
  сотни и снимает с позиции для отхода на шоссе. <br>
  <br>
  В это время наметом подскакивает Вахмистр сотни Мацкевич с двумя казаками, выяснявший 
  положение. <br>
  <br>
  Докладывает командиру о переходе Пулеметной к[оман]ды и предлагает вести сотни 
  к перешедшим. Но Сотник Башкиров, не доверяя непроверенным сведениям, не соглашается. 
  Тогда Вахмистр Мацкевич командует: «Сотня за мной!» Казаки в замешательстве... 
  какой делать выбор. Несколько всадников вырываются из строя и исчезают за Вахмистром 
  в кустарнике. <br>
  <br>
  Пишущий этот очерк Штаб-трубач полка находился при помощнике командира полка 
  Есауле М.С. Петрове (так как командир полка был вызван накануне е Штаб группы 
  в село «Арта»). <br>
  <br>
  Мы объезжаем противоположный фланг для выяснения обстановки. <br>
  <br>
  Подъезжаем к поскотине, стоит пулемет «Гочкис», красноармейцы обстреливают Атамановцев 
  - удивляясь, что они не отвечают огнем. Нам ясно, что Атамановцы ждут нас на 
  этом фланге. Проезжая дольше по фронту выясняем, что красноармейцы обеспокоены 
  полученными сведениями о переходе казаков и готовятся к отступлению. <br>
  <br>
  В это время на взмыленном серо-яблочном коне подскакивает дежурный по нашему 
  полку юнкер Оренб[ургского] Каз[ачьего] У[чили]ща Скударнов и докладывает Есаулу 
  Петрову: - Пулеметная команда перешла! - Командир 2-й сотни сотник Балатов снял 
  с позиции сотню и отводит на шоссе! С 1-й сотней, командиром которой был сотник 
  Балатов, связь потеряна! 3-ей сотни, находившейся в д. Улеты, - нет, по-видимому 
  тоже перешла! <br>
  <br>
  Получив это донесение, Есаул Петров, дежурный по полку и я - скачем карьером 
  к расположению полка, где и встречаем Вахмистра Мацкевича, собирающего одиночных 
  казаков, не могущих ориентироваться. Вахмистр Мацкевич доложил командиру, что 
  по сведениям, полученным от казаков, Пулеметная к[оман]да, 1-я и 3-я сотня перешли, 
  а 2-ю сотню командир увел в тыл и связаться с ней нет времени, надо выбираться 
  самим. <br>
  <br>
  Собрав группу в 20 казаков, мы скачем в направлении перешедшей Пулеметной к[оман]ды 
  через небольшую поляну, опушка которой занята вновь прибывшим батальоном красной 
  пехоты. <br>
  <br>
  Красные знают о переходе казаков, а потому скачем полным карьером. <br>
  <br>
  Завидя нас, пехота открыла пулеметный и ружейный огонь. Скачка продолжается: 
  летит под копыта лошадей красная лента с бантами, украшавшая сигнальную трубу 
  штаб-трубача и сорванная под одобрение казаков. <br>
  <br>
  Бог милостив! Без крупного урона: ранен казак и 2 лошади, проскочили зону обстрела! 
  Проскакав версты три, наша группа выбралась на дорогу. Впеpeди видна колонна 
  всадников. Оказались наши казаки и дружинники. <br>
  <br>
  Подъезжаем. Раздается команда. <br>
  <br>
  - Смирно! равнение направо! <br>
  <br>
  Начальник Пулеметной к[оман]ды Подхор[унжий] Панчехин скачет навстречу и рапортует 
  Есаулу Петрову, что команда в количестве 35 каз[аков] при 4-х пулеметах и 45 
  казаков из сотен перешли. Ес[аул] Петров здоровается, поздравляет с переходом, 
  подает команду «вольно» и казаки бурно поздравляют друг друга. <br>
  <br>
  Здесь выясняется вся картина неудавшегося перехода всего полка. Оказывается 
  было условлено, что Пол[ковник] Провахенский начнет операцию с переходом только 
  через 2 дня, но он поторопился и поэтому командиры наших сотен а также и многие 
  казаки, так как дело держалось в секрете, не были поставлены в известность о 
  дне перехода. <br>
  <br>
  Однако все могло бы сложиться и хуже для полка, так как начальнику Штаба группы 
  было известно содержание секретной записки, посланной накануне вечером с заставы 
  из д. Улеты, с указанием о дне перехода. Поэтому-то военный комиссар и перебросил 
  полк на другой фланг. <br>
  <br>
  Стечение обстоятельств омрачило казаков. Зная желание перейти оставшихся у красных, 
  было решено выручить наш полк. Обратились к командиру Атамановского полка с 
  просьбой - вновь начать наступление при нашем участии, так как мы, зная общую 
  обстановку, сможем выручить казаков и нанести серьезное поражение красным. <br>
  <br>
  Но предложение наше отвергнуто. <br>
  <br>
  Тогда Есаул Петров с согласия командира конной Дружины партизан казаков (очень 
  сочувственно к нам отнесшихся) обратился вторично к полк[овнику] Провахенскому, 
  прося разрешить ему на собственный риск с отрядом своих казаков и Дружиной партизан, 
  самостоятельно проделать следующую операцию: переправиться на противоположную 
  сторону реки Ингоды, пробраться в тыл красным, связаться с полком, выручить 
  его и, при возможности, нанести красным удар. Но и на это разрешение мы не получили. 
  <br>
  <br>
  Полк. Провахенский приказал всему отряду отойти в село Черемхово, в котором 
  находился Атамановский полк, в нем мы были тепло встречены Атамановцами и отведены 
  на квартиры на отдых. <br>
  <br>
  По истечении нескольких дней нас построили для смотра. <br>
  <br>
  Полк[овник] Провахенский объявил нам благодарность от имени Атамана Семенова 
  и приказ о производстве за переход всех офицеров в следующий чин, юнкеров в 
  офицеры, а казаков в следующие звания. На следующий день мы были приглашены 
