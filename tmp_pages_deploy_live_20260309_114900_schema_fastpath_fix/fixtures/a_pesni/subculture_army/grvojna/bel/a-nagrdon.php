	<br><span class="text"><span class="header"><strong>ДОКУМЕНТЫ 
        О НАГРАДАХ И ОБМУНДИРОВАНИИ ДОНСКОЙ АРМИИ</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., 2005, «Посев», 2005, стр. 68</em>.<br>
  <br>
  <br>
  <strong>Приказ <br>
  №1 <br>
  Круга спасения Дона </strong><br>
  <br>
  1 мая 1918 года<br>
  <br>
  §1<br>
  <br>
  ...Донская армия очнулась и сформировалась и нельзя дисциплинированным, сильным 
  духом и телом полкам и батареям ходить в одежде, напоминающей банды разбойников, 
  не иметь этих воинских отличий в бою, где эти отличия, разграничивающие командный 
  состав от подчиненного, представляют особо ценное значение. В виду этого Круг 
  спасения Дона приказывает немедленно всем казачьим частям, участвующим в защите 
  Дона, в отличие от большевистских банд, а равно всем воинским учреждениям и 
  заведениям, принять свой воинский вид и одеть, кому положено, погоны и прочие 
  знаки отличия; ношение погон и других знаков отличия вменяется в обязанность 
  и всем военным чиновникам... <br>
  <br>
  §2<br>
  <br>
  Круг спасения Дона постановил: 1) учредить знак военного ордена Георгиевский 
  крест с гербом Донского войска — для награждения за боевые отличия в борьбе 
  с большевиками, по представлениям начальников; 2) учредить особый знак-жетон 
  в память исторических дней заседаний Круга спасения Дона — для 1 мая 1918 года 
  членов этого Круга и всех, кто принимал участие в защите Донского Края... <br>
  <br>
  «Донской Край». 1918 г. №10. 03(16).05. <br>
  <br>
  <br>
  <strong>Приказ <br>
  Всевеликому войску Донскому <br>
  №366 </strong><br>
  <br>
  25 июня 1918 года<br>
  г. Новочеркасск<br>
  <br>
  При сем объявляю описание форм обмундирования, вооружения и снаряжения для генералов, 
  штаб- и обер-офицеров и казаков постоянной Донской армии, которым и руководствоваться, 
  за исключением таблицы №1, где графы о погонах, кантах к ним, кантах на обшлагах 
  и карманах, на воротниках мундира и шинели и цвета башлыка, впредь до решения 
  Большого Войскового Круга, исполнению не подлежат. Погоны носить прежнего образца: 
  синие с алым кантом с номерами, в пластунских полках — малиновые, в калмыцком 
  конном полку — желтые, в артиллерийских и инженерных частях — алые. <br>
  <br>
  Донской атаман, генерал-майор Краснов<br>
  Управляющий Военным и Морским отделами, Генерального Штаба генерал-майор Денисов 
  <br>
