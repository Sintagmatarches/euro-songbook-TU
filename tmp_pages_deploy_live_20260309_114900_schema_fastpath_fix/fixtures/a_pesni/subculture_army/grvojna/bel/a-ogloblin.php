	<span class="text"><strong>П.А. Новиков</strong><br>
  <br>
        <span class="header"><strong>НЕПОДЧИНЕНИЕ АТАМАНА П. П. ОГЛОБЛИНА</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 268-269</em>.<br>
  <br>
  <br>
        23 декабря 1918 г. Омске состоялось совещание при помощнике Военного министра 
        по казачьим делам по вопросу признания иркутских казаков отдельным войском. 
        20 мая 1919 г. атаман Иркутского казачьего войска П.П. Оглоблин подал 
        А.В. Колчаку <a href="a-materirk.php">доклад об утверждении Иркутского 
        казачьего войска</a> с приложением подробной исторической справки и набора 
        уставных документов. <br>
  <br>
  Несколько строк о самом атамане Прокопии Петровиче Оглоблине. Родился в 3 июля 
  1872 г. в Иркутске. В 1897 г. окончил Иркутское военное училище. Службу начал 
  в Забайкальском казачьем войске, участвовал в войнах с Китаем 1900-1901 гг. 
  и Японией в 1904-1905 гг. В 1914 г. есаул 1 Нерчинского казачьего полка (командир 
  - барон П.Н. Врангель) на германском фронте. В этом же полку сотнями командовали 
  Г.М. Семенов и Р.Ф. Унгерн фон Штернберг. На Персидском фронте командир 3 Верхнеудинского, 
  затем 1 Сунженско-Владикавказского казачьих полков, участвовал в наступлении 
  на Мосул. Начальник штаба, а затем командир бригады Урминского отряда. Награжден 
  орденами до Св. Анны 2-й ст., Св. Георгием 4-й ст., китайским и бельгийским, 
  царской благодарностью за атаку у с. Нугреева. В чин генерал- майора произведен 
  по статуту Георгиевского креста со старшинством с 26 ноября 1917 г.<sup>1</sup> 
  В 1917 г. Оглоблин избран атаманом иркутских казаков. Участвовал в декабрьских 
  боях 1917 г. в Иркутске, в 1918 г. в рядах Особого Маньчжурского отряда, с 8 
  октября по 19 ноября — начальник штаба V Приамурского корпуса. <br>
  <br>
  10 июня 1919 г. Совет министров правительства А.В. Колчака узаконил статус Иркутского 
  казачьего войска, а его атаман был подчинен непосредственно Военному министру 
  через его помощника по казачьим делам генерал-майора Б.И. Хорошкина. Войсковому 
  атаману П.П. Оглоблину по положению об управлении казачьими войсками подчинялись 
  все казачьи части его войска, а сам он получал права командира отдельного корпуса. 
  Получив должность, Оглоблин стал демонстрировать свои возможности. <br>
  <br>
  В Иркутске 13 июня 1919 г. представитель находившегося в Омске Оглоблина заявил, 
  что Иркутский казачий полк не признает власть командующего войсками Иркутского 
  военного округа генерал-лейтенанта В.В. Артемьева. После безрезультатных переговоров 
  личный состав полка с помощью 2 батальонов при 4 орудиях был разоружен и арестован<sup>2</sup>. 
  <br>
  <br>
  Считая, что командир Иркутского казачьего полка полковник П.И. Войлошников не 
  соответствует должности, а командир Сводной казачьей бригады генерал-майор И.Ф. 
  Шильников игнорирует его, Войскового атамана, П.П. Оглоблин подал рапорт об 
  удалении П.И. Войлошникова. В ответ командующий Иркутским военным округом указал 
  на недопустимость вмешательства в строевую часть полка. Однако, находясь в Омске, 
  Оглоблин все-таки назначил командиром Иркутского казачьего полка полковника 
  Бычкова. Приехавший в Иркутск Бычков встретил противодействие В.В. Артемьева 
  и И.Ф. Шильникова. <br>
  <br>
  Конфликт обострился 7 июля, когда П.П. Оглоблин приказом №1 «предложил» П.И. 
  Войлошникову и И.Ф. Шильникову не посещать полк и назначил временно командующим 
  полком войскового старшину Могилева. С подачи Оглоблина 8 июля Войсковое правление 
  исключило из казачьего сословия начальника штаба Сводной казачьей бригады есаула 
  Кубинцева, а также подъесаула пехоты Гундлаха и хорунжего Жизненского. <br>
  <br>
  8 июля командующий Иркутским военным округом генерал-лейтенант В.В. Артемьев 
  был вынужден отдать приказ: «Войсковой атаман Иркутского казачьего войска, генерал-майор 
  Оглоблин, домогаясь не предоставленной ему законной власти, позволил себе приказом 
  по Иркутскому казачьему войску от 7 июля за №1 (секретно) §1 самовольно и без 
  законных оснований отрешить от должности неподчиненного ему командира Иркутского 
  казачьего полка полковника П.И. Войлошникова и выделить названный полк из состава 
  отдельной Сводной казачьей бригады. Усматривая в этих самочинных и антидисциплинарных 
  действиях генерал-майора Оглоблина, в корне нарушающих начала законности и служебного 
  авторитета подлежащего войскового начальника, признаки деяния предусмотренного 
  статьями 141 и 145 XXII книги Свода военных положений 4-е издание, на основании 
  пунктов «А» и «Б» статьи 2 постановления Совета министров от 24 январи 1919 
  г., 319, 470-474 статей XXIV книги Свода военных положений отменяю явно противозаконный 
  приказ по Иркутскому казачьему войску от 7 июля №1, предписываю всю возникшую 
  по этому поводу переписку передать военному следователю 1-го Иркутского участка 
  для привлечения генерал-майора Оглоблина к законной ответственности, причем 
  мерой пресечения ему способов уклониться от следствия и суда, избираю содержание 
  под домашним арестом»<sup>3</sup>. Были арестованы и другие члены Войскового 
  правления: помощник атамана есаул С.А. Лукин<sup>4</sup>, старший адъютант хорунжий 
  Пежемский, заведующий войсковыми мастерскими К.Н. Лукин, представитель войска 
  в Омске С.М. Мелентьев и казначей П.П. Хороших. <br>
  <br>
  Но и с арестом П.П. Оглоблина склока продолжилась. С 19 июли по 6 августа 1919 
  г. в Иркутске проходили заседания чрезвычайного казачьего Круга. Почти 20 дней 
  42 делегата от проживавших в Иркутске казаков обсуждали отношении с командованием 
  Иркутского военного округа. И это тогда, когда по городу расклеивали воззвания 
  А.В. Колчака об отчаянном положении на фронте. <br>
  <br>
  28 июля в Иркутск, по настоянию П.П. Оглоблина, прибыла чрезвычайная комиссия 
  по расследованию вопросов иркутского казачества. В ее постановлении Оглоблин 
  обвинялся по статьям 141, 145, 110, 112, 319, 470 и 474 Свода военных положений 
  «в ниспровержении существующей власти». Из-за расследования в начале августа 
  была задержана отправка против енисейских партизан 2 сотни Иркутского казачьего 
  полка<sup>5</sup>. <br>
  <br>
  Если учесть военно-политическую обстановку лета 1919 г., конфликт В.В. Артемьева 
  и П.П. Оглоблина иначе как абсурдным назвать нельзя. У каждой стороны была своя 
  логика и оправдательные аргументы, но в итоге проиграли все. Прекрасно зная, 
  что малочисленное Иркутское казачье войско не имеет людей для формирования даже 
  одного полного шестисотенного полка, Оглоблин, тем не менее, занялся дележом 
  портфелей. А ведь только в борьбе с большевиками с их политикой расказачивания 
  Иркутское казачье войско могло сохраниться и пополниться новыми людьми. Вместо 
  этого имела место бессмысленная возня, завершившаяся переходом в январе 1920 
  г. на сторону эсеров и большевиков. <br>
  <br>
  В апреле 1920 г. П.П. Оглоблин объявил, что «состоя войсковым атаманом Иркутского 
  казачьего войска, [он] выявил себя противником павшей власти Колчака, за что 
  совместно с бывшими членами войскового правления, офицерами и казаками полка 
  и был арестован». Вот так, ни больше ни меньше - жертва режима Колчака, из рук 
  которого получил все полномочия и огромные возможности. Однако, каковы бы ни 
  были истинные мотивы П.П. Оглоблина, «заседавшие» в тылу рядовые иркутские казаки 
  тоже были бесполезны, если не вредны, для белых. <br>
  <br>
  Следует указать и на смягчающий фактор. Из-за нерешенного вопроса поземельного 
  устройства иркутские казаки имели на душу в среднем по 5-6 десятин, что было 
  меньше соседних крестьянских наделов и несопоставимо с 46 десятинами у забайкальских 
  казаков. Как следствие, еще до революции 1917 г. иркутские казаки «не будучи 
  в состоянии поддержать свое хозяйство, выходили на службу без лошадей, обмундирования... 
  или входили в долги и разорялись»<sup>6</sup>. <br>
  <br>
  Весной 1920 г. большевики позволили атаману иркутских казаков П.П. Оглоблину 
  провести 5-й съезд Иркутского казачьего войска. 8 марта съезд, «собравшись в 
  то время, когда сыны родного казачества проливают кровь в борьбе с забайкальской 
  реакцией, шлет привет власти, опирающейся на родные трудовые массы... Казачество 
  верит, что Советская власть сохранит за казаками тот особый уклад внутренний 
  жизни, ту самобытность, за которую казачество всегда боролось с царским правительством»<sup>7</sup>. 
  Однако лояльные резолюции не помогли и в начале апреля иркутские большевики 
  объявили об изъятии казачьих земель в пользу государства, у казаков отобрали 
  все помещения. В конце апреля был арестован и П.П. Оглоблин. Проведя полгода 
  в омском лагере, он, как не нанесший Красной армии вреда, был выпущен и эмигрировал 
  в Китай в г. Шанхай. <br>
  <br>
  В апреле 1925 г. в Шанхае был создан Восточный казачий союз, членом правления 
  которого от Иркутского казачьего войска стал Оглоблин. Союз оказывал материальную 
  поддержку казакам и их семьям. В марте 1926 г. в нем возникла склока из-за невыдачи 
  Оглоблину ссуды в 50 долларов, необходимой для оплаты квартиры и одежды<sup>8</sup>. 
  П.П. Оглоблин умер в Шанхае 21 августа 1940 г. <br>
