  Дня за два до восстания контрразведка штаба Иркутского военного округа перешла 
  в гостиницу «Модерн». В эсеровской организации работали поручик Курдяев и солдат 
  Ф. Цыганков. Начальник контрразведки Черепанов дал ордер на обыск в доме №77 
  на 2-й Иерусалимской улице, где было собрание районной организации иркутских 
  эсеров. <br>
  Офицеры контрразведки и солдаты отряда особого назначения застали 16 человек, 
  все были арестованы. Распоряжался на обыске помощник начальника контрразведки 
  чиновник Базанов. Арестованный там же на другой день прапорщик Окладников указал 
  на главный штаб организации. В земельном отделе было арестовано 21-22 человека. 
  <br>
  «В виду тревожного положения из «Модерна» политических арестованных перевели 
  в Оренбургское училище (казачье юнкерское - А.В.) и, когда арестованные были 
  еще в «Модерне», то поднимался вопрос о предании их всех военно-полевому суду. 
  Дня за два до перемирия генерал Сычев приказал по телефону штабс-капитану Черепанову 
  отправить арестованных к генералу Скипетрову. Черепанов ответил, что по словесному 
  распоряжению арестованных отправить не может, тогда генерал Сычев приказал начальнику 
  штаба гарнизона капитану Люба написать бумагу, предписание, что последний и 
  сделал. Черепанов, Носонов и Базанов из числа всех арестованных составили список 
  на более важных в политическом отношении, в который вошли 31 человек». <br>
  Капитаном Люба было сообщено в письменной форме (л.д. № 1) начальнику контрразведывательного 
  отдела штаба Иркутского военного округа, что начальник гарнизона приказал в 
  срочном порядке эвакуировать всех политических арестованных, передать [их| командированным 
  офицерам из отряда генерала Скипетрова для эвакуации на Восток. Штабс-капитан 
  Черепанов назначил [сопровождающим] офицера для поручений поручика Полканова, 
  сдал политических арестованных, причем одновременно с сим, рапортом генералу 
  Скипетрову Черепанов донес (л.д. № 42), что согласно отношения начальника гарнизона 
  г. Иркутска от 2 января с.г. за № 637, препровождает при поручике Полканове 
  31-го арестованного (политических) и доносит, что они были арестованы часть 
  на Графо-Кутайсовской улице, эсеров, большевиков и Политическому центру [так 
  в тексте — В.А.], причем первые являются одними из главных организаторов Иркутского 
  восстания. Арестованных в предполагаемый день не отправили, а были отправлены 
  они в день перемирия, а накануне, когда арестованных предполагали (отправить 
  — В.А.) пароходом, их предварительно связывали веревками по двое за локти. Потом 
  было приказано их развязать, так как пароход ушел. <br>
  На следующий день Черепанов [приказал — В.А.] арестованных не связывать, так 
  как было холодно и можно было опасаться, что арестованные замерзнут. Всего было 
  14 подвод; конвоя, арестованных и сопровождавших было человек 80. Приехав в 
  дер. Патроны и не застав парохода, все двинулись в село Лиственничное, а там 
  Черепанов потребовал пароход «Кругобайкалец», который и перевез всех на ст. 
  Байкал. На ст. Байкал всех арестованных принимали штабс-капитан Годлевский и 
  штабс-капитан Колчин, который был начальником гарнизона и комендантом ледокола 
  «Ангара», а второй был его помощником (д. 157-162)». <br>
  31 декабря 1919 г. приказом начальника штаба Западного отряда штабс-капитан 
  Годлевский был назначен начальником гарнизона и транспорта на озере Байкал. 
  5 января арестованные были пересажены с «Байкальца» на ледокол «Ангара». Распоряжался 
  Годлевский, принявший их от Черепанова по списку. Годлевский опрашивал каждого 
  арестованного. Первую ночь чины контрразведки ночевали на «Ангаре», но на другой 
  день Годлевский всех выгнал, остался с ним только штабс-капитан Колчин. 24 декабря 
  (6 января) ледокол пошел в Лиственничное. На ледоколе также находились: солдат 
  Особого маньчжурского отряда Михаил Карапетов, в каюте — полковник Сипайло, 
  капитан Грант, двое казаков и двое штатских. Арестованных стали выводить и дали 
  подписать бумагу с обязательством в течение 3 суток выехать из Сибири. Арестованных 
  раздевали до белья, говоря, что дадут другую одежду. Били — полковник Сипайло 
  и казаки, выводили на палубу. В Лиственничной были минут 10, потом пошли обратно 
  и продолжили выводку арестованных. «Выведенные наверх арестованные ставились 
  в одном белье около борта кормы и казак Лукин сзади ударял по разу или по два 
  каждого арестованного колотушкой по голове, а Сипайло, Грант и Годлевский держали 
  наизготовке револьверы по направлению ставшего арестованного и каждому из арестованных 
  говорили: «Подходи», - и после того, как Лукин ударял колотушкой по голове, 
  то наблюдатели из контрразведки Маньчжурской дивизии Бобасов и Молчанов, совместно 
  с Лукиным, сбрасывали арестованного через борт в воду». (С. 20.) Позади «Ангары» 
  шел «Кругобайкалец» и палачи старались, чтобы с него не заметили расправу. Вещи 
  были разобраны офицерами и солдатами. После расправы палуба была вымыта Карапетовым. 
  «Во время убийства криков арестованных слышно не было, так как их заглушал шум 
  ломающегося льда. На корме парохода имелась одна-единственная лампочка, которая 
  во время убийства была отвернута так, что на корме ледокола было темновато». 
  (С. 21.) <br>
