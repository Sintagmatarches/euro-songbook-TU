<br>
Слова Василия Жуковского<br>
<br>
Раз в крещенский вечерок<br>
Девушки гадали:<br>
За ворота башмачок,<br>
Сняв с ноги, бросали;<br>
Снег пололи; под окном<br>
Слушали; кормили<br>
Счетным курицу зерном;<br>
Ярый воск топили;<br>
В чашу с чистою водой<br>
Клали перстень золотой,<br>
Серьги изумрудны;<br>
Расстилали белый плат<br>
И над чашей пели в лад<br>
Песенки подблюдны.<br>
<br>
Тускло светится луна<br>
В сумраке тумана –<br>
Молчалива и грустна <br>
Милая Светлана.<br>
«Что, подруженька, с тобой?<br>
Вымолви словечко;<br>
Слушай песни круговой,<br>
Вынь себе колечко.<br>
Пой, красавица: «Кузнец,<br>
Скуй мне злат и нов венец,<br>
Скуй кольцо златое;<br>
Мне венчаться тем венцом,<br>
Обручаться тем кольцом<br>
При святом налое».<br>
<br>
«Как могу, подружки, петь,<br>
Милый друг далёко;<br>
Мне судьбина умереть<br>
В грусти одинокой.<br>
Год промчался – вести нет;<br>
Он ко мне не пишет;<br>
Ах! а им лишь красен свет,<br>
Им лишь сердце дышит…<br>
Иль не вспомнишь обо мне?<br>
Где, в какой ты стороне?<br>
Где твоя обитель?<br>
Я молюсь и слезы лью!<br>
Утоли печаль мою,<br>
Ангел-утешитель».<br>
<br>
1811<br>
<br>
