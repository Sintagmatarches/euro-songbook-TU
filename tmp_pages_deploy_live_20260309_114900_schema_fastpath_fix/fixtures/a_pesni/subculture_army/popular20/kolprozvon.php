	  И откелева куда?<br>
	  - С петроградской я темницы,<br>
	  Лет двенадцать там сидел.<br>
	  Час двенадцатый пробил,<br>
	  Стал я в форточку смотреть.<br>
	  Посмотрел я в ету форточку,<br>
