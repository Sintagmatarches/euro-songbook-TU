Молитеся вы, женщины,<br>
За ваших сыновей, -
<br>
    <br>

пели женщины, и в грубом и нежном плетении их голосов хорошо выделялся низки, вторящий контральто Марьи Фроловны:
<br>
    <br>

Мой старший сын, старик седой,<br>
Убит был на войне...
<br>
    <br>

