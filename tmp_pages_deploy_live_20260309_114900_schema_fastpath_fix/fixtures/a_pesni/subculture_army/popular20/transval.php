        <strong>Г. Галина, или Галина Галина</strong> - псевдоним  Глафиры Адольфовны Эйнерлинг (1873-1942, 
        урожд. Мамошина, по второму мужу - Гусева-Оренбургская). Дочь петербургского 
        учителя, по окончании гимназии работала телеграфисткой. Печаталась с 1895 
        г. Писала стихи, в том числе для детей, сказки, занималась переводами. 
        Осенью 1899 г. создала стихотворение &quot;Бур и его сыновья&quot;, 
        которое вскоре стало песней &quot;Трансваль, Трансваль, страна моя...&quot;. 
        За сочувствие студенческому движению (авторство крамольного стихотворения 
        <a href="../starrev/lesrub.htm">&quot;Лес рубят...&quot;</a>, 1901) выслана 
        из Петербурга. В 1905-1907 гг. продолжала публично читать гражданские 
        стихи. После 1917 г. отошла от общественной деятельности. Умерла в Ленинграде.<br>
  <br>
  <br>
        <strong>Англо-бурская война 1899-1902 гг.</strong><br>
  <br>
        Война шла за контроль над территорией современной ЮАР. Первые европейские 
        колонисты (в основном, голландцы, но также немцы и французы), высадились 
        на юге Африки в 1652 г. и основали Капскую колонию. Их потомки назвали 
        себя бурами или африканерами и рассматривали эти земли как свою родину. 
        В 1806 г. колонию захватила Великобритания; буры ушли из нее и в середине 
        XIX века основали на отвоеванных у африканцев землях Оранжевое свободное 
        государство (1854) и республику Трансвааль (1856). В 1867 г. на этих 
        территориях нашли золото и алмазы, что предрешило участь бурских республик. 
        В 1877 г. Трансвааль была аннексирована Великобританией, но вскоре добилась 
        возвращения части прав. В ходе войны 1899-1902 годов обе республики буров 
        пали и были превращены в английские колонии. 7 тыс. буров погибли в бою, 
        от 18 до 28 тыс. женщин и детей буров умерли в английских концентрационных 
        лагерях. В 1910 г. бывшие бурские земли и английские колонии объединились 
        в Южно-Африканский Союз под властью Англии; с 1961 г. - независимая 
        Южно-Африканская Республика.<br>
  <br>
        На судьбу основного населения Южной Африки - коренных народов зулу, коса, 
        басуто и др. - результаты англо-бурской войны не повлияли. Эти народы 
        добились признания своих человеческих прав только сто лет спустя, в последние 
        годы XX в.: массовые протесты чернокожего населения (составляющего ок. 
        87%) в 1980-е гг. вынудили парламент ЮАР в 1991 году отменить расовую 
        сегрегацию по месту жительства и владению землей, а с 1994 г. чернокожие 
        южноафриканцы получило право голоса. Парламентские выборы в том же году выиграла 
        партия Африканский национальный конгресс, и ее лидер Нельсон Мандела, 
        легендарный борец за права африканцев, стал первым черным президентом 
        ЮАР.<br>
