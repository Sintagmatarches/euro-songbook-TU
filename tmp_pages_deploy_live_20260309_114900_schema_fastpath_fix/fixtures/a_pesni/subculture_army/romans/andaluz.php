  Андалузская ночь горяча, горяча,<br>
  В этом зное и страсть, и бессилье,<br>
  Так что даже спадает с крутого плеча<br>
  От биения груди мантилья!<br>
  <br>
  И срываю долой с головы я вуаль,<br>
  И срываю докучные платья,<br>
  И с безумной тоской в благовонную даль,<br>
  Вся в огне, простираю объятья...<br>
  <br>
  Обнаженные перси трепещут, горят, -<br>
  Чу!.. там слышны аккорды гитары!..<br>
  В винограднике чьи-то шаги шелестят<br>
  И мигает огонь от сигары:<br>
  <br>
  Это он, мой гидальго, мой рыцарь, мой друг!<br>
  Это он - его поступь я чую!<br>
  Он придет - и под плащ к нему кинусь я вдруг,<br>
  И не будет конца поцелую!<br>
  <br>
  Я люблю под лобзаньем его трепетать<br>
  И, как птичка, в объятиях биться,<br>
  И под грудь его падать, и с ним замирать,<br>
  И в одном наслаждении слиться.<br>
  <br>
  С ним всю ночь напролет не боюсь никого -<br>
  Он один хоть с двенадцатью сладит:<br>
  Чуть подметил бы кто иль накрыл бы его -<br>
  Прямо в бок ему нож так и всадит!<br>
  <br>
  Поцелуев, объятий его сгоряча<br>
  Я не чую от бешеной страсти,<br>
  Лишь гляжу, как сверкают в глазах два луча, -<br>
  И безмолвно покорна их власти!<br>
  <br>
  Но до ночи, весь день, я грустна и больна,<br>
  И в истоме всё жду и тоскую,<br>
  И в том месте, где он был со мной, у окна,<br>
  Даже землю украдкой целую...<br>
  <br>
  И до ночи, весь день, я грустна и больна<br>
  И по саду брожу неприветно -<br>
  Оттого что мне некому этого сна<br>
  По душе рассказать беззаветно:<br>
  <br>
  Ни подруг у меня, ни сестры у меня,<br>
  Старый муж только деньги считает,<br>
  И ревнует меня, и бранит он меня -<br>
  Даже в церковь одну не пускает!<br>
  <br>
  Но урвусь я порой, обману как-нибудь<br>
  И уйду к францисканцу-монаху,<br>
  И, к решетке склонясь, всё, что чувствует грудь,<br>
  С наслажденьем раскрою, без страху!<br>
  <br>
  Расскажу я ему, как была эта ночь<br>
  Горяча, как луна загоралась,<br>
  Как от мужа из спальни прокралась я прочь,<br>
  Как любовнику вся отдавалась.<br>
  <br>
  И мне любо тогда сквозь решетку следить,<br>
  Как глаза старика загорятся,<br>
  И начнет он молить, чтоб его полюбить,<br>
  Полюбить - и грехи все простятся...<br>
  <br>
  Посмеюсь я тайком и, всю душу раскрыв,<br>
  От монаха уйду облегченной,<br>
  Чтобы с новою ночью и новый порыв<br>
  Рвался пылче из груди влюбленной.<br>
