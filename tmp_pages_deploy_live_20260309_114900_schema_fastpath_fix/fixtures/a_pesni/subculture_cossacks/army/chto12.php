	<h1>ЧТО 
ДВЕНАДЦАТОГО ГОДА…</h1>
	
<br>
Что двенадцатого года <br>
Поседелые орлы, <br>
Православному народу <br>
Заслужили славы вы. <br>
<br>
Взоры мужеством пылают. <br>
Грудь отвагою полна. <br>
Память славы созывает <br>
На поля Бородина. <br>
<br>
За живое нас задело, - <br>
С верой Богу помолясь, <br>
Вся Россия закипела <br>
И к знаменам собралась. <br>
<br>
Как по воле басурманской <br>
Ото всех концов земли, <br>
Словно волны океана <br>
Тьмы народов натекли. <br>
<br>
"Смерть!" - сказал врагам Кутузов <br>
И с дружиною своей <br>
Начал потчевать французов <br>
По-казачьи, без затей! <br>
<br>
На пригорке батарея <br>
Шлет им ядра на поклон, <br>
Зашаталися злодеи, <br>
Захмелел Наполеон. <br>
<br>
Пушки медны побросали; <br>
Русски грянули ура! <br>
Показали мы французам, <br>
Как столицу Москву брать! <br>
