	<h1>НУ-КА, 
  ВСПОМНИТЕ, РЕБЯТА… </h1>
  <br>
  Ну-ка, вспомните, ребята,<br>
  Как стояли в Зырянах,<br>
  Как не раз Хаджи-Мурата<br>
  Мы пугали на горах.<br>
  <br>
  Вот тогда случилось дело,<br>
  И куда не хорошо!<br>
  Басурманское все племя<br>
  Возмутилось заодно.<br>
  <br>
  Дружно, дружно налегали<br>
  На Аварский наш отряд.<br>
  Пули, ядра и картечи<br>
  Посылали, словно град.<br>
  <br>
  Но нам пули те знакомы,<br>
  Нам и ядра нипочем,<br>
  Встретить мы везде готовы<br>
  Неприятеля штыком.<br>
  <br>
  Как проворный басурманин<br>
  Вздумал шутку подшутить<br>
  Продержал нас на заставе,<br>
  Стал нас голодом морить.<br>
  <br>
  Он посты держать заставил,<br>
  Чтоб нас голодом сморить.<br>
  Он построил батареи,<br>
  Начал с пушечек палить.<br>
  <br>
  Мы рогатую скотину<br>
  Начисто перевели.<br>
  Стали есть мы лошадину –<br>
  И варили, и пекли.<br>
  <br>
  Вместо соли мы солили<br>
  Из патрона порошком.<br>
  Вместо трубочек курили<br>
  Из орудия дымком.<br>
  <br>
  Обносились, оборвались, -<br>
  С плеч свалилось все долой.<br>
  Тут-то мы хлопот набрались,<br>
  Чтоб управиться с зимой.<br>
  <br>
  Мы рогожи надевали,<br>
  Вместо бурок и плащей.<br>
  Ноги в кожи обували,<br>
  В кожи съеденных коней.<br>
