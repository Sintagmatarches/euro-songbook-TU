  Каждые две последние строки повторяются<br>
  <br>
 Записана летом 1964 на хуторе Камышеваха в совхозе «Заречный» 
  Ново-Кубанского района от казака Травкина Харлампия Акимовича, 1889 г. р. <br>
   <br>
  Песня была известна во всех частях Кубанского войска и в Кавказских полках. 
  В песне ведется рассказ о том, как 18 ноября – 17 декабря 1843 г. Аварский отряд 
  был окружен Хаджи Муратом. Песня походная, плясовая. Исполнялась в пешем и конном 
  строю на мотив <a href="soldatuch.php">«Солдатушки, браво, ребятушки»</a>. 
  Песня исполняется также и казаками ст. Отрадной.<br>
  <br>
  Варавва И.Ф. Песни казаков Кубани. Краснодар, 1966, № 25. </span><br>
