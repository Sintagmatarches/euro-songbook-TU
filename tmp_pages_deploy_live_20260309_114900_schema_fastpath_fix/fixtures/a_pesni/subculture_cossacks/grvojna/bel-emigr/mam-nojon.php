	<span class="header"><strong>МАМАНТОВ-НОЙОН</strong><br></span>
Калмыцкая народная песня из времен Гражданской войны <br>
  <br>
  Мамантов-нойон, прекрасно-усый, <br>
  К победе нас вел всегда неизменно. <br>
  Однажды он поехал, здоровый и бодрый, <br>
  Говорить с чужими крепко и серьезно.<br>
  …Известили о смерти Мамантова-грозного. <br>
  Говорили — недолго любимец наш страдал. <br>
  Место вождя, сильного, талантливого<br>
  Занял неизвестный Павлов-генерал. <br>
  Павлов — начальник новый и слабый. <br>
  По Манычу родному с нами блудил. <br>
  Ударил мороз жестокий и сильный – <br>
  Продрогли наши кони, выбились из сил. <br>
  На продрогших конях, измученные походом, <br>
  Стали на ночлег в степи безлюдной. <br>
  Несчастье случилось с нашим отрядом – <br>
  Красным оставили Край свой родной. <br>
  Изгнанные врагом из родного Дона <br>
  Пустились мы в путь, неведомый и долгий. <br>
  И только искусством Тепкина-нойона<br>
  Мы преодолели поход этот тяжкий. <br>
  Но усилия и искусства вождей напрасны -<br>
  Настало время с Родиной расстаться. <br>
  И в минуту отчаяния, в час расплаты <br>
  Русские начали от нас отделяться. <br>
  Слезами обливаясь, в ущелье горном <br>
  Собратьев своих врагам оставили. <br>
  При этом расставании мучительном, грустном, <br>
  Грудь разрывалась от тоски и боли. <br>
  На берегу бушующего моря безбрежного коней –<br>
  Друзей своих верных — покинули. <br>
  Тяжко лишиться друга неизменного... <br>
  А бедные бросаются в воду за нами. <br>
  Сине-морские пароходы-великаны <br>
  По направлению Крыма нас помчали. <br>
  Высадились на берег, заполнили вагоны, <br>
  А сердце щемит от горя и печали. <br>
  Проходили мы по татарской деревне. <br>
  На небе торжественно сияло солнце. <br>
  Да увидит солнце наше страдание <br>
  И, услышав мольбу нашу, поможет в беде. <br>
  Сине-гривый серый конь мой - <br>
  Подарок татарина — старого друга. <br>
  Да поможет нам Бог Всесильный <br>
  Победить грозно-жестокого врага. <br>
  Тепкин-нойон — наш вождь молодой — <br>
  Соратников своих ныне оставил. <br>
  Да подумал ли ты об участи нашей, <br>
  Переселяясь в свой лучший мир? <br>
  <br>
  <em>Перевел с калмыцкого Ш. Б-в </em><br>
  Улан Залат. 15 марта 1928 г. <br>
  <br>
        <em>Альманах &quot;Белая Гвардия&quot; №8. &quot;Казачество России в белом 
        движении&quot;. М., &quot;Посев&quot;, 2005, с. 45</em>.<br>
<br>
  <br>
        Эмигрантская песня донских казаков Зюнгарского Калмыцкого полка. Дата 
        &quot;15 марта 1928&quot; - видимо, дата записи, а сама песня могла быть 
        сложена раньше, между ноябрем 1920-го и 1928 годом. Генерал Мамантов командовал 
        конным корпусом донцов.<br>
  <br>
        <strong>Зюнгарский полк</strong> (четыре сотни, впоследствии шесть сотен) 
        был сформирован в Донской армии из казаков-калмыков в мае 1918 года в 
        станице Константиновской. Его основу составили казаки Платовской, Бурульской 
        и Граббевской станиц, бывшие в Степном походе. Полк начал бои в Сальских 
        степях, прошел всю войну и покинул Россию из Крыма с Русской армией генерала 
        Врангеля. Это единственный полк Донской армии, который прошел всю Гражданскую 
        войну без расформирований, переформирований и смены названия, и смог переправиться 
        в Крым после Новороссийской катастрофы Денинина, - и вообще, это был один 
        из самых надежных белых донских полков. Подробнее см. <a href="../bel/a-zungar.php"> 
        &quot;Воспоминания о Зюнгарском полку&quot;</a> Санжи Балыкова (1927).<br>
