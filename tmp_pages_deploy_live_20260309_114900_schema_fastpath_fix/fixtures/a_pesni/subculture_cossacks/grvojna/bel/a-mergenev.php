	<span class="text"><strong>С.С. Балмасов</strong><br>
  <br>
        <span class="header"><strong>МЕРГЕНЕВСКИЕ СОБЫТИЯ МАРТА 1919 г.</strong><br>
        </span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 165-166</em>.<br>
  <br>
  <br>
  После сдачи Уральска в январе 1919 г., которой предшествовала почти годичная 
  оборона от превосходящих во много раз сил противника, Уральское казачье войско 
  временно потеряло свою былую боеспособность. Это напрямую было связано с тем, 
  что правившие здесь Войсковой Съезд и Войсковое правительство оказались некомпетентными 
  в вопросах ведения войны, одной из причин чего и стала сдача Уральска красным. 
  Так, например, зимние неудачи уральских казаков 1918-1919 гг. были, во многом, 
  результатом отсутствия позитивной работы Войскового Съезда и правительства, 
  поскольку пополнения из молодежи приходили на фронт совершенно необученными, 
  несмотря на существование соответствующих тыловых служб, которые должны были 
  этим заниматься. В санитарных службах царил полный беспорядок, а снабжение боевых 
  частей было неудовлетворительным при том, что при падении Уральска красным достались 
  значительные запасы снаряжения. Сами же тыловики имели отличное обмундирование, 
  в то время как боевые части почти не имели нормальной зимней одежды, которая 
  и досталась большевикам как трофеи. <br>
  <br>
  Красное командование отлично понимало, что казаки будут отчаянно драться за 
  свою столицу, а с ее падением сопротивление их понизится, и рассчитывало этим 
  воспользоваться. Уральск пал, а вместе с тем окончательно пал среди казаков 
  и авторитет Войскового Съезда и правительства. Казаки, не видя дальнейшего стимула 
  к сопротивлению, в значительной своей массе разошлись по домам. На фронте остались 
  лишь наиболее стойкие части Уральской армии, которые уже не могли сдержать натиск 
  наступавшей вниз по Уралу советской группировки, в которой заметно выделялась 
  знаменитая 25-я стрелковая дивизия В.И. Чапаева. В начале марта 1919 г. фронт 
  откатился до станицы Мергеневокая. Казалось, еще немного — и красные сбросят 
  уральских казаков в Каспий. Однако, в этот момент решимость сражаться стала 
  пропадать не только у казаков, но и у самих красных. Рядовым красноармейцам 
  перед взятием Уральска комиссары обещали, что после этого война для них кончится. 
  Однако, этого не происходило, что и стало одной из причин массового недовольства 
  среди рядовых красноармейцев. Поскольку в обозримом будущем при таком положении 
  дел надеяться на победу было тяжело, красное командование решило опробовать 
  на уральцах испытанный ранее на донцах и оренбуржцах прием — митингование, «братание» 
  и переговоры. И вот, в начале марта 1919 г. из занятой большевиками станицы 
  Горячкинской в Мергеневскую прибыла красная делегация. <br>
  <br>
  В это время сохранившие боеспособность уральские казаки решили заменить обанкротившиеся 
  во всех отношениях Войсковой Съезд и правительство атаманом, которым выбрали 
  В.С. Толстова, бывшего командира 5-го Уральского конного полка, одного из самых 
  талантливых командиров уральцев. <br>
  <br>
  Сразу после выборов атамана, которые прошли 11 марта, Толстов, получив от казаков 
  право распоряжаться жизнью всех и каждого на территории войска, выехал на фронт, 
  в станицу Мергеневская. Там в это время проходил огромный митинг. На нем было 
  много казаков-дезертиров, ушедших с фронта после падения Уральска. Сюда же прибыла 
  большевицкая делегация во главе с Кондэ, или, как называли его на Урале, «Кондаковым»<sup>1</sup>. 
  Целью приезда делегации было организовать «братание» и убедить казаков прекратить 
  борьбу, отказаться от выбранного накануне атамана и признать Советскую власть 
  под предлогом заключения мира<sup>2</sup>. Для дезертиров, боявшихся твердой 
  атаманской власти, это была настоящая находка и спасение. <br>
  <br>
  По выезде из станицы Сахарной Толстов приказал сотне юнкеров с батареей двигаться 
  в Мергеневскую, сказав: «Я выбран атаманом, в Войске завелась зараза, я ее хочу 
  вырвать с корнем». После этого Толстов, начальник штаба Уральской отдельной 
  армии Моторный и юнкера подошли к дому, где происходило совместное заседание 
  большевицких и казацких депутатов. Делегаты, собравшиеся в Мергеневе для «заключения 
  мира», призвали также всех находившихся здесь уральских казаков прийти на митинг. 
  Верные своему долгу боевые части это проигнорировали, но выслали нескольких 
  казаков для сбора информации. Но когда они попробовали опротестовать действие 
  казаков-предателей, пошедших на соглашение с коммунистами, им просто заткнули 
  рты. <br>
  <br>
  Когда Толстов и Моторный подошли к двери, они услышали слова выступавшего на 
  заседании дьякона Горячинской станицы казака Спирина, который убеждал представителей 
  полков сдаться красным, говоря, что позорно им бороться против того самого солдата, 
  «с которым мы сидели в одних окопах на германской войне и ели из одного котла». 
  <br>
  <br>
  Надо сказать, что не все из находившихся в то время в доме казаков поддержали 
  это предложение. Кое-кто говорил, что «нужно подождать, так как у нас теперь 
  есть атаман, который, приехавши, укажет, что нам надо делать». Спирин же продолжал 
  убеждать казаков: «довольно проливать кровь, против кого воюем! Что нам атаман, 
  он разъезжает себе по Гурьеву, пьет кофе с сахаром!» <br>
  <br>
  В этот момент Толстов в сопровождении Моторного и юнкеров вошел в избу и крикнул: 
  «Врешь, мерзавец! Я — атаман, вот он, здесь!» Как свидетельствует сотник Уральского 
  казачьего войска Киров, «впечатление, произведенное таким появлением, не поддается 
  описанию. Все смешались — и красные, и зачинщики-казаки бросились к окнам, но 
  под ними уже стояли казаки 1-го учебного полка с винтовками, а весь двор был 
  оцеплен юнкерами. В страхе многие начали прятаться в избе, доходя даже до того, 
  что лезли под стол»<sup>3</sup>. Особенно интересно то, что среди делегатов 

  на совместном с большевиками заседании были обнаружены и члены Войскового Съезда, 
  которые, как оказалось, приняли в переговорах самое активное участие<sup>4</sup>. 
  <br>
  <br>
  После этого Толстов приказал казакам, которые находились в Мергеневе в составе 
  боеспособных полков, державших фронт, отойти в сторону, а всех остальных приказал 
  арестовать. Из них на месте отобрали зачинщиков «братания», к которым присоединили 
  захваченную красную делегацию. Спирина, Кондэ и Ко выставили на дворе дома, 
  где они заседали. Перед ними выстроились юнкера. Командовал расстрелом сам Толстов, 
  приказавший: «расстрелять их сейчас же!»<sup>5</sup> Интересно отметить, что 
  при расстреле Спирин на коленях молил о пощаде, в то время как Кондэ, по данным 
  очевидцев, «вел себя мужественно и заявил: «Что ж, мы умрем, но знайте, что, 
  в конце концов, мы победим»<sup>6</sup>. <br>
  <br>
  После расстрела Толстов подошел к казакам, которые не были главарями переговоров 
  и «братания» с коммунистами, но пошли за ними. Он приказал им вырыть во дворе, 
  где произошел расстрел, яму для убитых. Когда она была вырыта, он приказал ее 
  углубить настолько, чтобы в ней хватило места и копающим. Момент был жуткий. 
  Все ожидали страшной развязки. Но этого не произошло. Здесь же, во дворе, они 
  зарыли своими руками трупы смущавших их предателей из войска и большевиков. 
  Толстов ограничился лишь тем, что приказал выпороть всех, кто участвовал в митинге 
  и в совместном с большевиками заседании. Сочувствующие Спирину и Кондэ получили 
  по 100 плетей, а все остальные, просто пришедшие туда — по 25 плетей. Однако, 
  военно-полевой суд, которому были преданы все оставшиеся в живых участники самовольных 
  переговоров с большевиками, также приговорил многих из них к расстрелу. <br>
  <br>
  Больше подобных попыток красные не повторяли до конца декабря 1919 г., когда 
  армии Колчака и Юденича уже были разгромлены, силы Деникина отошли за Дон, а 
  сами уральцы были прижаты к Каспийскому морю. Коммунисты были шокированы таким 
  оборотом дела, поскольку до этого они успешно организовывали «братания» с донцами, 
  оренбуржцами и забайкальцами. Поэтому красные, зная, что Толстов шутить не будет, 
  отказались от дальнейших попыток разложить уральских казаков. <br>
  <br>
  Толстов приступил к оздоровлению армии. За считанные дни порядок в Уральском 
  казачьем войске был Толстовым наведен. Благодаря ему, не только была восстановлена 
  армия, но и значи- тельно был увеличен ее численный состав. <br>
  <br>
  Данный эпизод интересен тем, что, к сожалению, столь решительные действия белогвардейского 
  командования были редки. Если сравнить то, что происходило в рядах армии Колчака 
  и в ее тылу с тем, что было у уральских казаков, то совершенно ясно, что разложение, 
  которое началось у первых и в конце концов погубило Белое дело на востоке России, 
  можно было минимизировать или даже вообще устранить теми решительными мерами, 
  которые применял в Уральском казачьем войске Толстов. В №5 «Белой Гвардии» Е.В. 
  Волков написал, что, якобы, казаки, в том числе и уральцы, дрались хуже остальных 
  белогвардейских подразделений на востоке России. Автор данной статьи не будет 
  долго останавливаться на этом высказывании и скажет лишь то, что уральские казаки 
  успешно били, и били не раз прославленную 25-ю стрелковую дивизию Чапаева, которая 
  в свою очередь, била колчаковские войска в мае-июне 1919 г. Кроме того, из Уральской 
  отдельной казачьей армии, которая стойко дралась против большевиков под его 
  руководством, были только единичные случаи перехода к красным. В то же время, 
  из армий Колчака на сторону советских войск переходили, особенно в конце 1919 
  г., целыми дивизиями. Это еще один аргумент в пользу того, что по сравнению 
  с уральскими казаками колчаковские силы очень сильно проигрывали в боевом отношении. 
  Остается только пожалеть, что на посту Верховного Правителя в Сибири оказался 
  добродушный и мягкий Александр Васильевич Колчак, так и не осмелившийся до самого 
  конца изменить политику по отношению к предателям, антиправительственным элементам, 
  казнокрадам в Омском правительстве вроде Зефирова и Ко, которая сводилась к 
  затягиванию судебных дел или высылке большевиков и эсеров из одного района Сибири 
  в другой. <br>
  <br>
  И как знать, как пошла бы борьба с большевиками, если б столь радикальные меры, 
  применяемые Толстовым, были обрушены на голову паразитирующей спекулянтской 
  массы в колчаковском тылу, умело и неукротимо разрушавшей его, большевиков и 
  эсеров вроде премьера омского правительства Вологодского, открыто ведущих разлагающую 
  агитацию, казнокрадов-министров, которые все вместе работали на победу Ленина? 
  Как развивались бы события, если бы атаман Краснов зимой 1918-1919 гг. принял 
  бы на вооружение тактику Толстова в своей Донской армии? Как известно, она потерпела 
  не военное поражение под Царицыным, а была разложена большевиками. Что было 
  бы на Восточном фронте, прояви атаман Дутов такую же, как и у Толстова решимость 
  по искоренению пораженческой заразы, найдись среди сибирских казаков подобный 
  лидер, который бы заставил их от мала до велика всецело отдаться борьбе против 
  большевиков? Ясно одно: прояви железную волю белогвардейцы на всех фронтах, 
  какую неоднократно показывал Толстов, результат войны был бы иным. <br>
