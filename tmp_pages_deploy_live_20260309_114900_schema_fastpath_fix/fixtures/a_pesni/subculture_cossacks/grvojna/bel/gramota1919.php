	<span class="header"><strong>ГРАМОТА 
  РОССИЙСКОГО ПРАВИТЕЛЬСТВА КАЗАЧЬИМ ВОЙСКАМ </strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 5</em>.<br>
  <br>
  <br>
  Российское казачество, всегда служившее твердой опорой государственности, своею 
  грудью, на протяжении многовековой истории нашего отечества, самоотверженно 
  прокладывало дорогу к расширению границ государства и крепко защищало эти границы 
  от внешних врагов, ограждая тем возможность народу Русскому устраивать и развивать 
  сою государственную жизнь. <br>
  <br>
  Выдвигая кордоны и заставы, присоединяя целые области, казаки совершали победоносное 
  шествие Российской государственности, обагряя кровью своею вновь завоеванные 
  пределы. Устремляясь по степям Европейской России на юг, к теплым морям, Черному 
  и Каспийскому, пробиваясь по непроходимым дебрям Урала, Сибири, Кавказа и знойным 
  степям Туркестана, казаки содействовали расширению пределов отечества до его 
  естественных границ — Балтийского, Черного морей и берегов Тихого и Ледовитого 
  океанов. <br>
  <br>
  На путях исторического развития нашего государства казаки всегда бесстрашно 
  принимали удары многочисленных врагов, творя великое дело собирания и оберегания 
  земли Русской. <br>
  <br>
  Ныне, в тяжелую годину небывалой в летописях истории государства Российского 
  смуты, казаки, оставшиеся верными заветам своих предков, со всею силою патриотического 
  порыва, с полным сознанием своего национального долга перед тяжело страдающей 
  Родиной, в первых рядах ведут беспрерывную борьбу с разрушителями Русского государственного 
  дела — большевиками за воссоздание единой великой России. <br>
  <br>
  В постоянной борьбе с внешними врагами казачество выработало и закалило в себе 
  дух подлинной демократии и сумело сочетать во внутреннем устройстве своего быта 
  и войсковой жизни начала самоуправления с идеей действительного служения государственности 
  и общему благу народному. <br>
  <br>
  Ныне Российское Правительство подтверждает, что оно считает незыблемыми все 
  правовые особенности земельного быта казаков, образа их служения, уклада жизни 
  и управления военного и гражданского, слагавшихся веками, видя в них прочный 
  залог дальнейшего развития и процветания боевой мощи казачества на пользу всей 
  страны. <br>
  <br>
  Российское Правительство, в полном соответствии с современным правосознанием 
  казачества, торжественно заявляет, что ближайшей его задачей будет издание закона, 
  гарантирующего: <br>
  <br>
  1. Сохранение установившихся на землях казачьих войск основ войскового самоуправления, 
  заключающихся в праве выбора Войсковых Кругов, Войсковых Атаманов, Войсковых 
  Правлений и самостоятельного в пределах закона решения всех вопросов внутренней 
  жизни и управления. <br>
  <br>
  2. Неприкосновенность занимаемых казачьими войсками земель и прав их на владение 
  и распоряжение этими землями. <br>
  <br>
  Российское Правительство верит, что всегда преданное Родине и началам государственности 
  казачество, сомкнув свои ряды, общими дружными усилиями всего народа Русского 
  свергнет иго большевистского самовластия и освободит сердце России — Москву 
  для продолжения строительства Великой, свободной и неделимой России. <br>
  <br>
  Дана в г. Омске, мая «1» дня 1919 года. <br>
