          <span class="text"> <span class="header"><strong> НАД ОЗЕРОМ ЧАЕЧКА ВЬЕТСЯ 
          </strong><br>
          </span> <br>
          Над озером чаечка вьется,<br>
          Ей негде, бедняжечке, сесть.<br>
          Лети ты в страну, в край далекий,<br>
          Снеси ты печальную весть. <br>
          <br>
          А там во лесу во дремучем<br>
          Наш полк, окруженный врагом:<br>
          Патроны у нас на исходе,<br>
          Снарядов давно уже нет. <br>
          <br>
          А в том во лесу под кусточком<br>
          Боец молодой умирал.<br>
          Поник он своей головою,<br>
          Тихонько родных вспоминал: <br>
          <br>
          - Простите, папаша, мамаша,<br>
          Отчизна – счастливая мать.<br>
          Уж больше мне к вам не вернуться,<br>
          И больше мне вас не видать. <br>
          <br>
          <em>Последние две строки повторяются</em><br>
          <br>
          <em>И. Ф. Варавва. Песни казаков Кубани. Краснодар, 1966, № 312. Записана 
          осенью 1964 г. в ст. Линейной от Николая Дмитриевича Конотопченко, 1901 
          г. р., участника гражданской войны. Песня создана на основе романса Е. 
          Буланина «Чайка» («Вот вспыхнуло утро…»). На Кубани встречаются более 
          ранние варианты этой песни о казаке из окруженного полка в Августовских 
          лесах.</em><br>
          <br>
          <a href="../../kazaki/kuban/Kuban.php"><img src="../../zona/avatar/kuban1.jpg" width="94" height="122" border="0"></a><br>
          
          <br>
          <br>
          Очень популярная в годы Гражданской войны песня, ее варианты бытовали на разных фронтах. Варавва указывает, что 
          создана на основе романса &quot;Чайка&quot; (ошибочно назвав 
          автора &quot;Е. Буланиным&quot;: автор слов этого романса - Елена Буланина, музыки - Е. Жураковский), но это невозможно, так как у <a href="../../romans/tchajka.php">&quot;Чайки&quot;</a> совершенно другой размер. На основе &quot;Чайки&quot; были сложены другие 
          песни Гражданской войны - например, сибирские <a href="svistokpar.php">&quot;Гудок 
          паровоза&quot;</a> (1918) и &quot;На смерть Погодаева&quot; (1920). А 
          песня &quot;Над озером чаечка вьется&quot; создана на основе песни <a href="../../rus-jap/pleschut.php">&quot;Плещут 
          холодные волны...&quot;</a> и местами повторяет ее интонации. Там тоже 
          есть чайки: <br>
          <br>
          <em>Миру всему передайте, <br>
          Чайки, печальную весть: <br>
          В битве врагу мы не сдались - <br>
          Пали за русскую честь!.. </em><br>
          <br>
          См. также   вариант, записанный в 1942 году под Сталинградом от сибирского пополнения - <a href="../../ww2/folk/nadozerom-ww2.php">&quot;Над озером чаечка вьется&quot; (1942 года)</a>. Другие песни Первой мировой и Гражданской войн на той же основе: приморская партизанская <a href="dujuthol.php">&quot;Дуют холодные 
        ветры...&quot;</a>, 
	
          <a href="../bel/umirsoldat.php">&quot;Умирающий солдат&quot;</a> из газеты Народной армии Комуча.<br>
          <br>
          Представленные здесь варианты записаны в основном от бывших красноармейцев, 
          но песня могла бытовать и у белых, и у красных, и у повстанцев 
          разных мастей. Песня возникла 
          в начале Первой мировой войны и связана с трагедией армии генерала Самсонова, 
          окруженной и уничтоженной в конце лета 1914 года в Восточной Пруссии. <br>
          <br>
          <br>
          <strong>ВАРИАНТЫ (3)<br>
          <br>
           1. Над озером чайка летает</strong><br>
          <br>
          Над озером чайка летает, <br>
          Все, бедная, ищет, где сесть. <br>
          Лети ты в Сибирь, в край далекий,<br>
          Снеси ты печальную весть. <br>
          <br>
          Скажи, что в лесу у дороги <br>
          Наш красный боец умирал, <br>
          Прикрытый он серой шинелью, <br>
          Тихонько родных вспоминал. <br>
          <br>
          Прощайте, мамаша, папаша,. <br>
          Прощай, молодая жена. <br>
          За счастье и волю народа <br>
          Враги расстреляли меня. <br>
          <br>
          <em>С сайта <a href="http://www.unn.ru/rus/f9/old/fond/" target="_blank">фольклорного 
          фонда им. проф. Морохина</a></em><br>
    <br>
    <br>
    <strong>2. Не вейтеся, чайки, над морем...</strong><br>
    <br>
Не вейтеся, чайки, над морем, <br>
Вам некуда, бедным, присесть. <br>
Летите в долину Сибири, <br>
Снесите печальную весть. <br>
<br>
В далекой восточной долине <br>
Врагом наш отряд окружен, <br>
За правду там бьются партизаны <br>
Совместно с советским полком. <br>
<br>
Патроны у них на исходе, <br>
Снаряды уж вышли давно,<br>
А помощи нет ниоткуда, <br>
Погибнуть нам здесь суждено. <br>
<br>
Вы видите, белые чайки, <br>
Подходит наш час роковой, <br>
Бежали партизаны в атаку, <br>
Поля покрывались врагом. <br>
<br>
Но в плен не сдавались живыми <br>
Партизаны в неволю врагу, <br>
А наши как братья остались <br>
Навеки у белых полков. <br>
