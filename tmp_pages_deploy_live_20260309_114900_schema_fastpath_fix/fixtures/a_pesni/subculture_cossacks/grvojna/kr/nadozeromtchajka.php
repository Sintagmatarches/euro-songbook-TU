        <strong><br>
        3. Не вейтеся, белые чайки</strong><br>
        <br>Не вейтеся, белые чайки,<br>
	    Вам некуда бедным присесть,<br>
	    Слетайте в долину Амура,<br>
	    Снесите печальную весть:<br>
	    «Далеко на западном фронте<br>
	    Наш полк окружен был врагом.<br>
	    Там дрался проклятый Семенов<br>
	    С восьмым партизанским полком.<br>
	    Патроны у нас на исходе,<br>
	    Снаряды уж вышли давно.<br>
	    Нам помощи нет ниоткуда,<br>
	    Погибнуть нам здесь суждено!<br>
	    Слетайтеся, белые чайки,<br>
	    Пропойте нам час роковой!»<br>
	    Так кончили битву амурцы,<br>
	    Все поле покрыто врагом.<br>
	    О, этот проклятый Семенов<br>
	    Так панику сильно держал,<br>
	    Оставил Читу и Даурию,<br>
	    А сам за границу бежал.<br>
