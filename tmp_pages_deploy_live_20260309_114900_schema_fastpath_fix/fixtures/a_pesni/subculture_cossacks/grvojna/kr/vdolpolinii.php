        <strong>ВАРИАНТЫ (2)<br>
        <br>
        1. Вдоль по линии Кавказа </strong><br>
        <br>
        Вдоль по линии Кавказа <br>
        Пролетал сизой орел, <br>
        Пролетал сизой орел. <br>
        Он летал перед войсками, <br>
        Издавал строгий приказ: — <br>
        Чтобы были у вас ружья, <br>
        Ружья новые в руках, <br>
        Ружья новы, шашки востры, <br>
        Револьверы в кабурах. <br>
        Мы поедем за границу, <br>
        Бить отеческих врагов, - <br>
        Часть порежем, часть побьем мы, <br>
        Остальных в плен заберем. <br>
        <br>
        <em>Багизбаева М. М. Фольклор семиреченских казаков. Часть 2. Алма-Ата: 
        «Мектеп», 1979, № 249. Записана от Воропаевой К. М., 1912 г. р., г. Иссык, 
        в 1976 г. Второй вариант дан там же. У Вараввы 2 редакции, публикуемый 
        нами текст относится ко второй редакции времен гражданской войны, хотя 
        конкретный адрес песней утерян. (Ср.: Варавва И. Ф. Песни казаков кубани, 
        Краснодар, 1966, № 63 и № 320)</em>.</em><br>
        <br>
        <a href="../../kazaki/semirek/Semirek.php"><img src="../../zona/avatar/semirek1.jpg" width="94" height="122" border="0"></a><br>
<br>
        Фраза <em>&quot;Мы поедем за границу, бить отеческих врагов&quot;</em> 
        эпохе Гражданской войны не свойственна, она попала по наследству от старых 
        вариантов песни. Для красноармейцев границ и &quot;отеческих врагов&quot; 
        не должно было существовать - в эпоху Гражданской они считали себя интернационалистами 
        и борцами за всеобщее счастье (по крайней мере, в теории), а не за счастье 
        &quot;отечества&quot;. &quot;Патриотическая&quot; терминолония начала 
        утверждаться позднее, по мере укрепления тоталитарного советского государства. 
        А белые внешними врагами не очень интересовались. <br>
