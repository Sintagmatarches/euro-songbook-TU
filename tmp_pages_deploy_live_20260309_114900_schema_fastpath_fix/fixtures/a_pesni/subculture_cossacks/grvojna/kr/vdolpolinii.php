        Вдоль по линии Кавказа <br>
        Молодой орел летал, <br>
        Он летал перед войсками, <br>
        Наш народный комиссар. <br>
        <br>
        Он с походом нас поздравил, <br>
        Отдавал строгий наказ:<br>
        «Чтобы были у вас, братцы, <br>
        Шашки вострые в руках, <br>
        <br>
        Ружья новые, винтовки — <br>
        Быстро справимся с врагом. <br>
        Часть побьем и часть изгоним, <br>
        Остальных мы в плен возьмем». <br>
        <br>
        <em>С сайта <a href="http://www.unn.ru/rus/f9/old/fond/">фольклорного 
        фонда им. проф. Морохина</a></em><br>
        <br>
        <br>
        Переделка старой казачьей песни, известной, по крайней мере, со времен 
        Кавказской войны 1817-1864 гг. - см. <a href="../../army/hupro.php">&quot;Разорение 
        аула Хупро&quot;</a> (1849), <a href="../../istor/kazakivboj.htm">&quot;Казаки 
        идут в бой&quot;</a> (Крымская война, 1853-56). По сути дела, это просто 
        старая песня, вставлен только &quot;народный комиссар&quot; (на этом месте 
        положено быть имени какого-нибудь актуального военачальника или &quot;белого 
        царя&quot;; в данном случае, это безымянный комиссар). Слово &quot;линия&quot; 
        - также рудимент, в годы Гражданской войны линий уже не было: в 16-19 
        вв. так назывались приграничные укрепленные районы (Уральская линия, Илецкая 
        линия, Кавказская линия и др. - отсюда &quot;линейное войско&quot;). См. 
        также чапаевский вариант - <a href="vdolurala.php">&quot;Вдоль по линии 
        Урала&quot;</a>.<br>
