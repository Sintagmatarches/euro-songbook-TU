	      </strong><br>
	      </span> <br>
	      В воскресенье мать-старушка<br>
	      К воротам тюрьмы пришла<br>
	      И в платке к родному сыну<br>
	      Передачу принесла.<br>
	      <br>
	      И привратнику сказала,<br>
	      Что утомилася она:<br>
	      - Ноги старые устали,<br>
	      Сорок верст пешком я шла.<br>
	      <br>
	      Передайте вы Ванюше!<br>
	      Всюду люди говорят,<br>
	      Что в тюрьме там заключенных<br>
	      Бьют и с голоду морят.<br>
	      <br>
	      Надзиратель усмехнулся:<br>
	      - Твой Ванюша осужден,<br>
	      Вчера утром на рассвете <br>
	      На спокой отправлен он.<br>
	      <br>
	      Осудили его чехи.<br>
	      У тюремной у стены,<br>
	      Когда приговор читали<br>
	      Знали звездочки одни.<br>
	      <br>
	      Повернулася старушка,<br>
	      Покачнулася слегка,<br>
	      К надзирателю тихонько<br>
	      Протянулася рука:<br>
	      <br>
	      - Передачу я купила <br>
	      На последние гроши,<br>
	      Передайте заключенным<br>
	      На помин его души.<br>
	      <br>
	      Повернулася старушка,<br>
	      От ворот тюрьмы пошла,<br>
	      И никто про то не знает,<br>
	      Что в душе она несла.<br>
	      <br>
	      Вот пришла домой старушка,<br>
	      Рассказала, где была,<br>
	      Обняла детей-сироток,<br>
	      Слезно плакать начала.<br>
	      <br>
	      - Вы, сиротки, вы сиротки,<br>
	      Ты, несчастная жена.<br>
	      Так за что же расстреляли<br>
	      Чехи родного сынка?<br>
	      <em><br>
          Записана А. Гуревичем от слепого певца в Иркутске, в предместье Маратово 
	        в 1935. Впервые: Фольклор Восточной Сибири. Сост. А. Гуревич. Иркутск, 
	        1938, стр. 22, текст № 66. Один из вариантов записан на Урале, см. уральский 
