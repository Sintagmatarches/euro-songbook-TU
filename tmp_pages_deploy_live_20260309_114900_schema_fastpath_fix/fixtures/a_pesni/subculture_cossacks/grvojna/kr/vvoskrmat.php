	      Усмехнулся надзиратель:<br>
	      «Твой сыночек осужден,<br>
	      Осужден прошедшей ночью,<br>
	      На покой отправлен он.<br>
	      Твово сына расстреляли<br>
	      У тюремной у стены;<br>
	      Когда приговор читали,<br>
	      Знали звездочки одни».<br>
	      Побледнела мать-старушка,<br>
	      Пошатнулася слегка,<br>
	      Ноги старые устали –<br>
	      Сорок верст пешком прошла.<br>
	      У надзирателя невольно<br>
	      Покатилася слеза.<br>
	      «Передачу я купила<br>
	      На последние гроши,<br>
	      Передай же передачу<br>
	      На помин его души».<br>
