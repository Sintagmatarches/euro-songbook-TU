	        Восточной Сибири эпохи гражданской войны. Улан-Удэ, 1957. С. 122-123.</em><br>
	      <br>
	      <br>
	      Песня об Александровском централе времен колчаковщины. Александровский 
	      централ служил тогда концлагерем. В годы Гражданской войны было сложено 
	      две песни о нем, обе на мотив песни <a href="../../popular20/dalekovstrane.php">&quot;Далеко 
	        в стране Иркутской</a>&quot;, созданной узниками Александровского централа 
	      1880-х гг. Вторая песня - <a href="valeksandr.php">&quot;В Александровском 
	        селеньи&quot;</a>, - рассказывает о восстании заключенных в декабре 1919 
	      года. После Гражданской войны песня сохранилась в блатном репертуаре - 
	      в частности, урки пели ее в сталинских лагерях.<br>
	      <br>
	      В вариантах обеих песен упоминаются чехи. Они оказались 
	      втянуты в Гражданскую войну в России в мае 1918 г. Чехословацкий корпус, 
	      сформированный в царской России в годы Первой мировой из военнопленных 
	      чехов и словаков - подданных Австро-Венгрии, был объявлен частью армии союзников и по условиям Брестского 
	      мира подлежал эвакуации из России через дальневосточные порты. Эшелоны 
	      корпуса растянулись по всей Сибири и востоку Европейской части России. В дороге 
	      у чехов случился конфликт с  пленными австрийцами, большевики попытались 
	      разоружить корпус, чехословаки оказали сопротивление и ликвидировали Советскую 
	      власть на территории своего следования, взяли Самару и установили там 
	      новое, эсеровское правительство - Комитет членов учредительного собрания. 
	      После колчаковского переворота 18-19 ноября 1918 г. чехи выполняли 
	      карательные функции (причем сохранились жалобы самих чехов, что под прикрытием 
	      чешских штыков белогвардейцы творят террор и беззаконие; с другой стороны, 
	      сохранились также подобные жалобы белогвардейцев на зверства чехов). <br>
	      <br>
          <br>
	      <strong>Расстрелы в истории России</strong><br>
          <br>
Расстрел как способ казни в России до начала Гражданской войны 1917-1922 
	    гг. применялся редко. Единственный период массовых расстрелов в царской 
	    России - подавление революции 1905-1907 гг. Тогда прозвучали два известных приказа карательным войскам: генерала 
	    Ф. Трепова в Петербурге &quot;Патронов не жалеть!&quot; и командира Семеновского 
	    полка полковника А. Мина в Москве &quot;Арестованных не иметь!&quot;<br>
        <br>
Официально все это время смертная казнь в России была отменена, за исключением особых случаев. Ее отменила 
	    императрица Елизавета - фактически с 1741 г., формально указом от 30 сентября 1754 г. 
	    Указ Екатерины II от 6 апреля 1775 г. подтвердил указ Елизаветы, однако <em>исключая случаи особо тяжких преступлений </em>(этой оговоркой воспользовались 
	    при казни декабристов). После смерти Екатерины смертную казнь еще раз 
	    упраздняет Павел I указом от 13 апреля 1799 г. 1-я 
	    Государственная дума 16 мая 1906 г. единогласно  принимает законопроект об отмене смертной казни, однако он не успевает стать законом, а Дума вское распускается. До 1905 г. казнили, в очень редких случаях, 
	    политических или военных преступников, но через повешение, а не через расстрел. В русском дореволюционном фольклоре как правило &quot;рубят 
	    голову&quot; или &quot;вешают&quot;. <br>
        <br>
В очередной раз смертная казнь исчезла в результате Февральской революции, но затем 
	    Временное правительство вернуло ее на фронте. Практика же массовых расстрелов 
	    вновь стала применяться в годы Гражданской войны. 
	    См. также другую старую песню о расстреле: <a href="../../popular20/kaznmor.htm">&quot;Казнь 
	      моряка&quot;</a>.<br><br>
