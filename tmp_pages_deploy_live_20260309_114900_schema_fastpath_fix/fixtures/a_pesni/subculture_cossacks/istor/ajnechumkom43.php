  <br>
АЙ, НЕ ШУМКОМ-ТО ШУМИТ</h1>
  <br>
  Ай, не шумком-то шумит дубро... ай, не дубровушка, е-ай, <br>
  <div class='refren'>Вот шумит шата... всё шатается, <br>
  Ой-да, как шатался там, <br>
  Волочился вот добрый мо... добрый молодец, </div>
 Ай, волочился там, добрый мо... ай, добрый молодец, е-ай, <br>
   <div class='refren'>Вот и, больно ра... больно раненый, <br>
  Ой-да, по чужой дальней, <br>
  По чужой, по дальней сторо... по сторонушке, </div>
  Ай, по чужой-то, дальней сторо... ай, по сторонушке, е-ай, <br>
  <div class='refren'>Вот и, пришатну... пришатнулся <br>
 Удал добрый молодец <br>
Ко крутому, младец, ко бе... он ко бережку, </div>
  Ай, ко крутому, младец, ко бе... ай, он ко бережку, е-ай, <br>
  <div class='refren'>Вот и, он кричи... он кричит-гичит, <br>
   Удал добрый молодец, <br>
  Да кричит же он громким го... громким голосом, </div>
  Ай, ну, кричит же он громким го... ай, громким голосом: &quot;Е-ай, <br>
 <div class='refren'>Ой-да, уж вы бра... вы, братцы мои, <br>
  Братцы перевощички, <br>
  Перевезите на ту сторо... на сторонушку, </div>
  Ай, перевезите на ту сторо... ай, на сторонушку, е-ай, <br>
<div class='refren'>Ой-да, как за это, вот, братцы мои, <br>
  Заплачу вам сто рублей: <br>
  Если мало вам, дам всю ты... дам всю тысячу, </div>
  Ай, если мало вам, дам и ты... ай, дам и тысячу, е-ай, <br>
  <div class='refren'>Ой-да, мало ты... мало тысячи, <br>
  Братцы перевощички, <br>
  Вы возьмите мово коня, вот конёчика, </div>
  Ай, вы возьмите мово коня, ай, вот конёчика, е-ай, <br>
<div class='refren'>Ой-да, схороните меня молодца, <br>
 Братцы перевощички, <br>
 Между трёх дорог, в перекрё... в перекрёсточке, </div>
  Ай, между трёх дорог в перекрё... в перекрёсточке, е-ай, <br>
 <div class='refren'>Ой-да, между Ки... между Киевской, <br>
 Вот и, между Питерской, <br>
 А и третьей, братцы, Черни... все Черниговской, </div>
  Ай, между Питерской да Черни... ай, все Черниговской, е-ай, <br>
 <div class='refren'>Ой-да, в голова... в головашечках <br>
    Поставьте мово коня, <br>
   В руку правую, дайте ша... дайте шашечку, </div>
  Ай, в руку правую шашку во... ай, шашку вострую, е-ай; <br>
<div class='refren'>Ой-да, кто пройдёт тут, остановится, <br>
  Вот и, кто проедет тут, <br>
    Кто проедет, тот подиву… подивуется,</div>
  Ай, кто проедет, тот подиву… ай, подивуется ай подивуется, е-ай,<br>
  <div class='refren'>Ой-да, что лежит тут, лежит молодец,<br>
 Удалой разбойничек,<br>
 Люду бедному он защи… он защитничек,</div>
  Ай, люду бедному сам защи… ай сам защитничек, е-ай,<br>
 <div class='refren'>Ой-да, лютый недруг всем насильникам,<br>
 Ой-да, по прозваньицу<br>
 Степан Разин-то Тимофе… Тимофеевич&quot;.</div>
