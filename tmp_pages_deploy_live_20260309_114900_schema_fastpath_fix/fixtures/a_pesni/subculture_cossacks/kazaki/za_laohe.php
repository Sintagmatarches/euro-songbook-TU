  <br>
  За рекой Ляохэ</strong><br>
  <br>
  За рекой Ляохэ загорались огни<br>
  Грозно пушки в ночи грохотали<br>
  Сотни юных орлов из казачьих полков<br>
  На Инкоу в набег поскакали. <br>
  <br>
  Пробиралися там день и ночь казаки,<br>
  Миновали и горы, и степи<br>
  Вдруг вдали у реки засверкали штыки<br>
  Это были японские цепи. <br>
  <br>
  И без страха отряд поскакал на врага<br>
