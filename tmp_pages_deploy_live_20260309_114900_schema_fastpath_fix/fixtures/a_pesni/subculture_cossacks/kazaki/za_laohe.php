<br>
  <a href="http://www.pobeda.ru/content/view/3616/142" target="_blank"><em>&quot;Малая энциклопедия 
  русской военной песни&quot; И. Савина</em></a><br>
  <br>
  <br>
  <strong>Штурм Инкоу</strong><br>
  <br>
  Потеряв в декабре 1904 года Порт-Артур, русские попытались провести крупный 
  рейд по тылам японцев для разрушения их коммуникаций. Был отправлен сборный 
  кавалерийский отряд генерала Мищенко. Груженный тяжелыми тюками, с артиллерией, 
  он продвигался слишком медленно для эффективного набега - японцы успевали приготовиться 
  к встрече. В новогоднюю ночь в пешем строю отряд попытался взять штурмом станцию 
  Инкоу в устье реки Ляохэ. От русского артобстрела загорелись склады возле Инкоу, 
  и в атаку пришлось идти свете пожаров, на виду у окопавшейся японской пехоты. 
  Штурм провалился, русские отошли с большими потерями. <br>
  <br>
  <br>
  <strong>Из сочинения генерала Петра Краснова &quot;Картины былого Тихого Дона&quot; 
  (1909):</strong><br>
  <br>
  &quot;Так, в постоянных стычках и набегах маленькими партиями проводили казаки 
  зиму 1904 года. 24 декабря генерал-адъютант Мищенко получил приказание произвести 
  набег в тыл японцам по направлению к городу Инкоо, откуда японцы получали продовольствие. 
  В набег были назначены 21 сотня уральцев и забайкальцев, 11 сотен кавказских 
  горцев, 19-й, 24-й и 26-й донские казачьи полки, Черниговские, Нежинские и Приморские 
  драгуны, 4 сотни пограничной стражи, полусотня разведчиков, 4 конные охотничьи 
  команды, 12 орудий забайкальских казачьих батарей, 6 конных орудий 20-й батареи 
  и 4 пеших орудия.<br>
  <br>
  27 декабря, четырьмя колоннами - генерала Самсонова с драгунами, войскового 
  старшины Свешникова с 1500 вьюков под охраной забайкальцев, генерал-майора Абрамова 
  с уральцами, забайкальцами и конными охотниками стрелковых полков и генерал-майора 
  Телешова с донцами, кавказцами и забайкальскими орудиями, - конный отряд перешел 
  линию наших постов и пошел вдоль реки Ляохе, направляясь к Инкоо.<br>
  <br>
  28 декабря донцы, широко раскинув лаву, выгнали японцев из деревни Калихе и 
  продолжали двигаться далее на юг. В этот же день донские сотни сделали набег 
  на железную дорогу и разрушили на ней телеграф и шпалы на протяжении 250 саженей. 
  В то же время, в разных местах казаки начали захватывать китайские арбы с продовольствием 
  для японцев. Продовольствие это сжигали. Так работая в тылу у японцев, конный 
  отряд прошел в три дня около 110 верст и 30 декабря подошел к станции Инкоо.<br>
  <br>
  Для взятия станции были назначены спешенные казаки от разных полков, в том числе 
  пошло и две сотни донцов. Штурм был назначен ночью. Наша артиллерия зажгла склады 
  фуража, бывшие подле Инкоо, и они пылали громадным костром, освещая местность 
  на большом протяжении. Полковник Харанов, командовавший отрядом спешенных казаков, 
  повел их вдоль реки Ляохе. Казаки скользили на льду, часто падали, сбивались 
  в кучи. Плохо обученные действиям в спешенном порядке, незнакомые с силою теперешнего 
  огня, они скоро начали падать ранеными и убитыми. Как бабочки на огонь, они 
  шли на пылающие склады по освещенному месту, направляясь к окопам, занятым японской 
  пехотою. Оставалось около 900 шагов до станции. С дружным криком &quot;Ура!&quot; 
  побежали казаки на штурм. Пачечный огонь японцев слился в один общий треск и 
  гул. Много казаков тут упало. Живые прилегли к земле, а одиночные смельчаки 
  прокрались к самому полотну дороги и ко рвам возле построек и били по японцам 
  на выбор. Отдохнувши немного, казаки снова бросились к станции, но опять страшный 
  огонь японцев заставил их остановиться. Стало ясно, что без громадных потерь 
  овладеть станцией невозможно. Станция была окружена волчьими ямами и проволочными 
  заграждениями...<br>
  <br>
  Пришлось отступать. Там, сзади, в темноте ночи, у деревни Лиусигоу трубач играл 
  сбор и туда брели казаки, огорченные и озлобленные постигшей их неудачей. Но 
  противник, должно быть, тоже понес немало и немало был напуган. Он не преследовал. 
  Казаки подбирали раненых и убитых и выносили их с поля битвы. Всего за этот 
  кровавый ночной штурм мы потеряли убитыми 4 офицеров и 57 казаков и драгун, 
  ранеными 20 офицеров и 171 казака и драгуна, без вести пропало - вероятно, убитыми 
  - 26 казаков.<br>
  <br>
  Штурм, хотя и неудачный, большой станции, разрушение железной дороги во многих 
  местах, взорванные и сожженные обозы и склады с продовольствием, появление повсюду 
  в тылу у японцев смело рыскающих казаков напугало японцев. Отовсюду мчались 
  подкрепления. Пять японских батальонов чуть не бегом бежали от станции Ташичао, 
  чтобы отрезать нам путь отступления. Сзади, у Ньючуана уже располагался большой 
  отряд из пехоты, конницы и артиллерии. Японцы готовили нам ловушку. Лед на реке 
  Ляохе был ненадежен. Нужно было спешить к своим. Но генерал Мищенко не желал 
  бросать раненых, тем более, что японцы так ненавидели казаков, что прирезывали 
  их, - и вот, 31 декабря, только в 9 часов утра удалось выступить и тронуться 
  на север. Шли тремя колоннами. Ближайшей частью к японцам были донцы генерала 
  Телешова, в середине шла колонна генерала Абрамова и в западной колонне шли 
  драгуны генерала Самсонова. Раненых везли при средней колонне.<br>
  <br>
  Японцы все время следили за колонной генерала Телешова, но донцы легко их отгоняли.<br>
  <br>
  В ночь на 1-е января отряд генерала Мищенко с большим трудом переправился через 
  реку Ляохе и расположился на ночлег.<br>
  <br>
  Вечером, когда уже стемнело, колонна генерала Телешова подошла к деревне Синюпученза, 
  лежащей на правом берегу реки Ляохе, недалеко от слияния ее с рекой Тайцзыхе, 
  и стала располагаться на ночлег. Казачьи разъезды, находившиеся к северу от 
  этой деревни, донесли, что в деревне Сайтайзы, в пяти верстах от места ночлега 
  Донской дивизии, находится батальон японской пехоты; в то же время получено 
  было известие и о том, что из Ньючуана, то есть с юга, наступает японская пехота 
  с артиллерией. Таким образом. Донская дивизия становилась на ночлег между двух 
  сильных японских отрядов, имея сзади себя трудно проходимую широкую реку, покрытую 
  весьма ненадежным льдом. Офицер, посланный для связи со средней колонной генерала 
  Абрамова, не нашел ее на том месте, где она должна была быть по приказу, и донцы 
  остались одни в холодную темную ночь, без связи со своими. На землю спустился 
  густой туман. Ничего не было видно в пяти шагах и тускло мерцали в этом мраке 
  бивачные огни, разведенные во дворах китайских фанз. Донцы прикрывали повозки 
  с ранеными, и нужно было дать время им отойти, вот почему генерал Телешов, несмотря 
  на то, что к утру японцы могли усилиться и окружить его, отдал приказ расседлать 
  лошадей и ночевать спокойно, выставивши лишь усиленные меры охранения.<br>
  <br>
  Тревожная это была ночь! Туман был так густ, что китайские проводники отказывались 
  вести разъезды; офицер, посланный в штаб отряда, проездил всю ночь и вернулся 
  назад к утру, так и не найдя его. Ни звезд на небе, ни местных предметов на 
  земле, ничего, по чему можно было бы разобраться, не было видно. Китайцы-лазутчики 
  то и дело приходили из соседних деревень и таинственно сообщали о том, что японцы 
  усиливаются и готовятся к переправе через реку Ляохе.<br>
  <br>
  Наступало утро. Побелел туман и заходил волнами над темными бороздами гаоляновых 
  полей. И сейчас же от сторожевых застав пришли донесения о том, что японцы начали 
  наступление с двух сторон.<br>
  <br>
  Генерал Телешов приказал полковнику Багасву с 24-м донским казачьим полком и 
  полковнику Попову с 26-м донским полком, выйдя из деревни Синюпучензы - Багаев 
  на север, а Попов на юг, - встретить японцев и задержать их наступление, главные 
  же силы под командой генерала Стоянова должны были продолжать свое движение, 
  направляясь на северо-запад.<br>
  <br>
  Сотни 26-го полка с артиллерией быстро обстреляли японские цепи, только что 
  переправившиеся через реку Ляохе, и заставили японцев бежать обратно за реку. 
  После этого полковник Попов благополучно присоединился к главным силам.<br>
  <br>
  24-й донской полк двинулся в линии колонн на север, на деревню Утайцзы, куда 
  наступали и японцы. В густом тумане казаки и японцы подходили к одной и той 
  же деревне. Уже стали видны деревья ее садов, темными полосами показались глиняные 
  стенки и крыши фанз. Но вот порывы утреннего ветерка рассеяли немного туман 
  и подъесаул Коньков, шедший в передовом взводе, увидал в нескольких сотнях шагов 
  от себя японский головной отряд. Ни минуты не медля, помчались казаки, склонивши 
