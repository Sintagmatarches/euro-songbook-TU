  <br>
  Одна из песен, написанных в 1947-1953 гг. приятелями-москвичами Алексеем Охрименко, 
  Сергеем Кристи и Владимиром Шрейбергом. Ими же созданы <a href="../ww2/folk/jabylbataljon.htm">&quot;Батальонный 
  разведчик&quot;</a>, <a href="tolstoj.php">&quot;Жил-был великий писатель...&quot;</a> 
  и <a href="otello.php">&quot;Отелло&quot;</a> (Венецианский мавр Отелло...). 
  Песни эти получили широкое распространение как анонимные, в 1950-е гг. входили 
  в репертуар инвалидов, занимавшихся нищенствованием в электричках, в конце 1950-х 
  проникли в студенческий фольклор. Авторские тексты были опубликованы лишь в 
  начале 1990-х, и тогда же песни на большой сцене исполнил Алексей Охрименко 
  - последний к тому времени живой соавтор.<br>
