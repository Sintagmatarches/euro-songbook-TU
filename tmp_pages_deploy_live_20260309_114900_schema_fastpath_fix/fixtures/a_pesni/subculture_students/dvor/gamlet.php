  Алексей Охрименко, Сергей Кристи и Владимир Шрейберг<br>
  <br>
  Ходит Гамл<strong>е</strong>т с пистолетом. <br>
  Хочет когой-то убить, <br>
  Он недоволен белым светом, <br>
  Он думает: "Быть или не быть?" <br>
  <br>
  Евойная мать согрешила <br>
  И за другого пошла,<br>
  А сапогов еще не износила, <br>
  В каких она за гробом мужа шла. <br>
  <br>
  Офелия - Гамлетова девчонка <br>
  Спятила, товарищи, с ума. <br>
  Потому что датская сторонка <br>
  Для народа хуже, чем тюрьма. <br>
  <br>
  Спятила, в воду сиганула. <br>
  Даже не сняла с себя наряд, <br>
  И тут на кровь Гамлета потянуло, <br>
  И начал он калечить всех подряд. <br>
  <br>
  И по расписанию Шекспира <br>
  Вынимал тут Гамлет-молодец <br>
  С ножен комсоставскую рапиру, <br>
  Наступал Полонию конец. <br>
  <br>
  Был Гамлет упрям и непослушен, <br>
  За себя двух корешей послал. <br>
  Вскоре был один из них задушен, <br>
  А другой так без вести пропал. <br>
  <br>
  Со зла Гамлет затеял постановку, <br>
  Чтобы свои сомненья разрешить. <br>
  Во дворце устроить потасовку <br>
  И в общей свалке кой-кого убить. <br>
  <br>
  Решил разоблачить он королеву, <br>
  А главное - прихлопнуть короля, <br>
  Самому отдать концы налево, <br>
  А Данию оставить без руля... <br>
  <br>
  Весь двор смотреть собрался на пиесу, <br>
  Которой Гамлет автор-режиссер, <br>
  Король не проявлял к ней интересу, <br>
  Но под конец до смысла он допер. <br>
  <br>
  На сцене королева-потаскуха <br>
  Синильной мужа травит кислотой. <br>
  Льет из пипетки, лярва, прямо в ухо, <br>
  Душевною блистая красотой. <br>
  <br>
  Король смекнул да улыбнулся глупо, <br>
  И тут Гамлет давай их всех кромсать!.. <br>
  Наутро там нашли четыре трупа, <br>
  И то их невозможно опознать. <br>
  <br>
  Шекспир - в машину и на поле боя, <br>
  Он моментально трупы опознал <br>
  И, несмотря на приступ геморроя. <br>
  За сутки свою пьесу накропал. <br>
  <br>
  Больной Шекспир работал с увлечением. <br>
  Он делал деньги, затянув ремень, <br>
  И получили люди развлечение, <br>
  А автор - белый хлеб на черный день!<br>
