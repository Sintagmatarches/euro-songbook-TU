<h1>ПЕСНЯ О МИЛИЦИОНЕРЕ</h1>

Я в милиции служу.<br>
Синий я мундир ношу.<br>
С чёрной "дурой" на боку<br>
Я порядок стерегу.<br>
Гав-гав!<br>
<br>
Берегу порядок - гав! -<br>
Чтоб никто нигде не чгав!!!<br>
На подножках не стояв<br>
И под ноги не плював.<br>
Гав-гав!<br>
<br>
С нарушителей - гав-гав! -<br>
Я тотчас взимаю штраф,<br>
А не то пойдёте в глав,<br>
В отделение, гав-гав,<br>
Гав-гав-гав-гав!!!!<br>
<br>
<span class="i">Вот текст песни о милиционере - мне мама рассказывала, что они пели её с друзьями в конце 50-х, подростками
(исполняется с сильным украинским акцентом)<br>
<br>
    Из <a href="https://doxie-do.livejournal.com/16539.html" target="_blank">живого журнала писательницы Татьяны Сигаловой</a>,
    выпускницы Тартуского университета, 14.02.2012.<br>
</span>
<br>
<br>
По словам фольклористки Марии Ахметовой: мама Татьяны Сигаловой была, вероятно,
из средней полосы России; песня, скорее всего, написана на Украине,
но бытовала и за ее пределами, в том числе в студенческой среде; есть варианты на русском и украинском.<br>
<br>
Мария Ахметова, сообщено 27 декабря 2024:<br>
<br>
<div class="i">"Я эту песенку частично помню, опять же, со Старого Арбата, где ее пел юноша не то из Харькова, не то из Донецка
(он вообще пел на суржике, в том числе "Постой, паровоз..." со строками "Меня засмоктала  небезпечная багнюка" и обращением "кондухтер").
Там было так:<br>
<br>
Я в милиции служу,<br>
Форму серую ношу,<br>
С пистолетом (кобурою?) на боке<br>
Я порядок сторожу (навожу?).<br>
<br>
[Припев] Гав-гав, гав-гав, гав-гав, гав-гав, гав-гав, гав-гав, легав-гав-гав.<br>
<br>
[Второй куплет не помню, но там точно была строка "Шоб в парадных не стояв", поскольку аудиторией,
слушавшей пение именно в парадняке, сиречь в подъезде, это воспринималось с воодушевлением.]<br>
<br>
Я порушника поймав (запомнила именно так, хотя наверняка было "споймав")<br>
И с него (снимаю?) штраф.<br>
Ах, не хочешь? Дашь ответ<br>
    В отделении легав!"</div>
