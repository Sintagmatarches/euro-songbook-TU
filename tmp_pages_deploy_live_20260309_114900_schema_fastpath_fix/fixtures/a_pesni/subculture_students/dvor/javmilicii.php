    1. Я в милиции служу гав гав, гав гав...</div>
<br>
Я в милиции служу.<br>
Гав-Гав. Гав-Гав.<br>
Форму серую ношу.<br>
Гав-Гав. Гав-Гав.<br>
Охраняю тишину.<br>
Гав-Гав. Гав-Гав.<br>
<br>
Чтоб народ спокойно спав.<br>
Гав-Гав. Гав-Гав.<br>
И из окон не рыгав.<br>
Гав-Гав. Гав-Гав.<br>
А не то всех отведу.<br>
Гав-Гав. Гав-Гав.<br>
В отделение лягав.<br>
Гав-Гав. Гав-Гав.<br>
<br>
<span class="i">Я эту песенку с детства знаю.<br><br>
    У нас, во дворе, ментов звали только лягавыми и ни как по другому.<br><br>
    С милицией я познакомился очень рано и не в самом плохом месте. При советах были во дворах комнаты подростков, где можно было зимой сыграть в шахматы или шашки, был бильярд и даже телевизор. Это может смешно покажется, но в шестидесятых не у каждой семьи был телевизор, а в подростковой комнате был. Летом играли в волейбол и футбол мячами из подростковой комнаты и сюда же приходила женщина в форме лягавой и проводила с нами работу прививая нам <s>шизу</s> нормы и правила поведения в обществе. При всем при этом у нас еще был, в соседнем дворе, комсомольский оперативный отряд, который включал в себя воспитательную группу по работе с подростками и трудными детьми. Поэтому как таковой милиции я и не видел, а все эти заменители органов скорее всего и создавали впечатление лягавости и отталкивали от действительно народной милиции.<br><br>
Позже, по жизни, я ни с ментами ни с лягавыми не встречался и только когда был уволен из рядов ВС и пришел встать на учет в центр занятости, мне предложили стать участковым милиционером. Сработала защита на подсознательном уровне, связанная с тем, что мент или лягавый это как проказа ... участковым я не стал.<br><br>
Сегодня я уже плотно познакомился и с ментами, и с лягавыми и со всеми милицейскими категориями, порядочных среди них я не встретил ни одного. Даже к хорошо знакомым, с которыми выпил не один литр водки, у меня отношение к ним как к лягавым и я ни разу не слукавил перед ними, а всегда доказывал собутыльникам, что лягавыми не становятся, лягавыми рождаются.<br><br>
Вот это я написал.<br><br>
А всего-то хотел напомнить, что сегодня в Украине День участкового инспектора милиции. Мне поздравлять не кого, а вдруг у кого есть какой завалявшийся ментяра с которым вы собутыльничаете, не забудьте его поздравить и лишний раз напомнить ему, что он лягавый.<br>
