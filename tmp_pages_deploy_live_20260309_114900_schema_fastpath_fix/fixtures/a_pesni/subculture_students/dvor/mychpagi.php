        <strong>3. Песня про мушкетеров </strong><br>
        <br>
        Полночь, Париж уже спит, гвардейцы спят.<br>
        Три шпаги, свет луны в глаза слепят.<br>
        Мы слуги короля, мы, ой-ля-ля,<br>
        Задиры мушкетеры короля. <br>
        <br>
        Ботфортами стуча по кабакам,<br>
        Бокалами бургундского звеня,<br>
        Мы шпаги короля, мы, ой-ля-ля,<br>
        Красавцы мушкетеры короля.<br>
        <br>
        С гвардейцами фехтуем мы шутя,<br>
        Заколем их, у них судьба своя,<br>
        На кладбище свезем и, ой-ля-ля,<br>
        И выпьем за здоровье короля<br>
        <br>
        Мы любим королеву, тру-ля-ля.<br>
        Служанок обнимаем по ночам.<br>
        Мы Франции сыны, мы, ой-ля-ля,<br>
        Красавцы мушкетеры короля.<br>
        <br>
        Министры - шулера, король - дурак.<br>
        Шуты, шутя, играют в короля.<br>
        Но Франция жива, и, ой-ля-ля,<br>
        И живы мушкетеры короля.<br>
        <br>
        Уходят короли, идут года.<br>
        У них, у королей, судьба своя,<br>
        Но Франция жива, и, ой-ля-ля,<br>
        И живы мушкетеры короля. <br>
