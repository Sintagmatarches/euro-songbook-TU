	<span class="header"><strong>НА 
РАНЧО</strong> <br></span>
<br>
В далеких Кордильерах, на севере Техаса,<br>
Стояло одинокое ранчо,<br>
Старуха там жила крива и одноглаза,<br>
Гусей своих кормила саранчой.<br>
На лошади верхом, с семизарядным кольтом,<br>
Она гоняла в прерию гусей<br>
И крохотный загон с проводкой в 200 вольтов,<br>
Их охранял, чтоб не украл сосед.<br>
<br>
<em>Припев:</em><br>
<br>
На ранчо, на ранчо, скоро я вернуся<br>
Жили-были два гуся, у одной бабуси.<br>
<br>
А по соседству жил огромнейший верзила,<br>
Он виски жрал как лошадь, и притом,<br>
Он звался &quot;Черный Билл&quot;, и будучи блондином,<br>
Считался первым парнем на ранчо.<br>
Однажды поутру, лишь только солнце встало,<br>
И в прерии рассеялся туман,<br>
Старушка двух гусей, увы, не досчиталась,<br>
А это, согласитесь, не фонтан.<br>
<br>
Вскипело все внутрях у бедненькой старушки,<br>
Она хватает кольт свой поскорей,<br>
Вгоняет в ствол патрон и - к Билловой избушке,<br>
Готовая к изъятию гусей.<br>
Вот выстрел прогремел, и вздрогнула хибара,<br>
И Черный Билл едва успел сказать:<br>
На черта гуси мне? Ведь это - кукабара,<br>
На кости кукабары он упал.<br>
<br>
Старушка мчится в ночь к племяннику в Монтану,<br>
Поскольку поломата жизня вся.....<br>
Фургон, глухая ночь, и вдруг из-под банана<br>
Навстречу ей выходят два гуся.<br>
<br>
<em>Неизвестный источник</em><br>
<br>
<br>
