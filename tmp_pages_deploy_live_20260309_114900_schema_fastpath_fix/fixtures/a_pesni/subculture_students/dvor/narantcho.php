<strong>ВАРИАНТ<br>
<br>
Жили у бабуси...</strong><br>
<br>
Жили у бабуси <br>
Два веселых гуся: <br>
Один серый, другой белый - <br>
Два веселых гуся. <br>
<br>
В отрогах Кордильер на севере Техаса <br>
Стояло одинокое ранчо. <br>
Старуха там жила, хрома и косоглаза, <br>
Гусей она кормила саранчой. <br>
<br>
<em>Припев:</em><br>
<br>
На ранчо, на ранчо скоро я вернуся, <br>
Жили-были два гуся у одной бабуси. <br>
<br>
На лошади гнедой с семизарядным кольтом <br>
Она гоняла в прерию гусей. <br>
И каменный забор с проводкой в 200 вольтов <br>
Их охранял, чтоб не украл сосед. <br>
<br>
<em>Припев.</em> <br>
<br>
А по-соседству жил огромнейший детина, <br>
Он водку жрал, как лошадь, и при том <br>
Он звался Черный Билл, а так как был один он, <br>
То был он первым парнем на ранчо. <br>
<br>
<em>Припев. </em><br>
<br>
Однажды поутру, лишь только солнце встало, <br>
И в прерии рассеялся туман, <br>
Старушка двух гусей, увы, не досчиталась, <br>
А это, согласитесь, - не фонтан! <br>
<br>
<em>Припев. </em><br>
<br>
Вскипела в жилах кровь у бедненькой старушки, <br>
Она хватает кольт свой поскорей. <br>
Вгоняет в ствол патрон, чтоб в билловой избушке <br>
Готовой быть к изъятию гусей. <br>
<br>
<em>Припев. </em><br>
<br>
Вдруг выстрел прогремел, и дрогнула хибара, <br>
И Черный Билл едва успел сказать: <br>
- На черта гуси мне? совсем сдурела баба, - <br>
Но тут ему пришлося замолчать. <br>
<br>
<em>Припев.</em> <br>
<br>
Старушка едет прочь, к племяннику в Монтана, <br>
Поскольку поломата жизня вся. <br>
Стоит глухая ночь, как вдруг из-под банана, <br>
Выходят ей навстречу два гуся. <br>
<br>
Мыли гуси лапки <br>
В луже у канавки - <br>
Один серый, другой белый <br>
Спрятались от бабки. <br>
<br>
<em>Припев:</em><br>
<br>
На ранчо, на ранчо скоро я вернуся, <br>
Жили-были два гуся у одной бабуси.<br>
