        <br>
        <br>
        Возможна ли археологическая экспедиция, в которой не поют? Возможна ли 
        встреча археологов, на которой не спели бы или, как минимум, не помянули 
        бы ни одной песни? Ни сталкиваться с такими, ни слышать о них не приходилось. 
        Что поют — вопрос второй, но — поют. Не претендуя на полноту охвата материала, 
        попытаюсь вкратце описать песенный быт археологических экспедиций последних 
        двух-трех десятилетий.<br>
        <br>
        <strong>1</strong><br>
        <br>
        Любой субкультуре свойственны специфические черты, характеризующие ее 
        состав, тип отношений с окружающим миром, деятельность ее членов, их общие 
        пристрастия и интересы. При этом одним из наиболее ярких показателей для 
        характеристики субкультуры будет, любят ли петь и что поют входящие в 
        нее люди. И если на первый вопрос довольно легко ответить даже при минимальном 
        знакомстве с жизнью среды, второй требует длительного погружения в нее 
        или детального исследования. Репертуар, то есть устойчивый набор регулярно 
        исполняющихся любимых, значимых для данной группы людей песен, интересен, 
        прежде всего, для социолога и культуролога, так как позволяет выявить 
        истоки и культурные связи данной общности. Для человека же, не занимающегося 
        исследовательской деятельностью, знакомство с песенным репертуаром дает 
        шанс больше узнать о жизни социальной группы и, возможно, примкнуть к 
        ней. <br>
        <br>
        В настоящий момент представители субкультуры археологов — это не только 
        профессионалы-ученые, но и те, кто из года в год приезжает в экспедицию. 
        Более того, не каждый профессионал может быть сопричастным субкультуре: 
        многие из тех, чья деятельность не требует регулярных выездов в поле, 
        ограничиваются лишь профессиональным общением с коллегами. Важнейшим фактором, 
        характеризующим субкультуру археологов, является обязательное пребывание 
        в полевых условиях. Именно экспедиция, несмотря на то, что продолжается 
        она, как правило, не более трех месяцев, является средой, определяющей 
        специфику субкультуры в целом, и в частности — особенности ее песенного 
        репертуара. Пребывание в поле роднит археологов с геологами, туристами 
        различных направлений, то есть со всеми, кто выезжает в экспедиции, походы 
        и т. п. Ситуация &quot;поля&quot; благодаря изолированности, независимости, 
        оторванности от городских проблем не меньше, чем собственно раскопки, 
        привлекает к археологии профессионально далеких от нее людей. <br>
        <br>
        Процесс формирования, пополнения и расширения песенного репертуара также 
        происходит преимущественно в поле. В городе в период межсезонья он пребывает 
        в &quot;законсервированном&quot; виде и практически не изменяется до следующего 
        сезона. Существуют экспедиции, где репертуар остается стабильным в течение 
        многих лет. Фиксирована, закреплена традицией бывает даже последовательность 
        исполнения песен. Как правило, это экспедиции с постоянным ядром коллектива. 
        Встречаются, напротив, случаи, когда репертуар обновляется каждые 2—3 
        года, но обычно это связано с полной сменой полевого состава.<br>
        <br>
        <strong>2</strong><br>
        <br>
        Песенный репертуар большинства экспедиций начал формироваться в 50-е — 
        60-е годы. Этот период современной песенной культуры отмечен двумя важными 
        тенденциями: становлением авторской (самодеятельной, бардовской) песни 
        и очередным расцветом городского песенного фольклора. И та, и другая традиции 
        в большой степени были представлены в интеллигентской и студенческой среде. 
        И именно эти две традиции стали основными источниками песенного репертуара 
        археологических экспедиций. Однако, разумеется, ими активный репертуар 
        археологов не ограничивается. Назовем основные его составляющие:<br>
        <strong><br>
        1. Песни бардов.</strong> Прежде всего, следует отметить, что, являясь 
        авторскими, эти песни далеко не всегда осознаются таковыми в экспедиции. 
        Авторство многих даже широко известных песен не указывается в сборниках-песенниках 
        и часто не известно поющим. Несмотря на это песни бардов составляют значительную 
        долю репертуара большинства экспедиций. Они привносятся различными участниками 
        экспедиций в соответствии с их собственными вкусами и представляются на 
        суд всей &quot;братии&quot;. Правда, далеко не все из этих песен выдерживают 
        испытание временем и задерживаются дольше одного сезона (см. ниже).<br>
        <strong><br>
        2. Общераспространенный городской фольклор.</strong> Его роль в репертуаре 
        зависит от возраста членов экспедиции: для старшего поколения песни этой 
        группы связаны со студенческими годами и вызывают конкретные воспоминания, 
        а для молодежи они символизируют нечто &quot;древнее&quot;, причастность 
        к которому доказывается узнаванием и исполнением песни. Часть городского 
        песенного фольклора, усвоенная младшими поколениями вне экспедиции (в 
        том числе, еще в детстве), тоже в значительной мере представлена в экспедиционном 
        репертуаре, но не имеет подобной функции. Случается, что в экспедициях 
        поются и некоторые произведения традиционного фольклора, но выделять их 
        в отдельный источник было бы неправомерно, поскольку все они попадают 
        в репертуар экспедиций опосредованно — через городскую культуру .<br>
        <br>
        <strong>3. Песни из кинофильмов, мультфильмов, спектаклей; популярные 
        эстрадные песни.</strong> Они чаще исполняются молодежью и могут выполнять 
        функцию средства установления контакта, первичного узнавания в неустоявшихся 
        коллективах, а также использоваться в качестве материала для переделок.<br>
        <br>
        <strong>4. В последнее десятилетие-полтора добавились песни, связанные 
        с традицией русского рока. </strong>Эти песни редко перерастают пределы 
        одного-двух сезонов, будучи тесно связаны с конкретными исполнителями, 
        и уходят вместе с ними.<br>
        <br>
        <strong>5. Собственно фольклор археологов.<br>
        <br>
        3</strong><br>
        <br>
        Формирование, развитие и сохранение песенного репертуара каждого коллектива 
        — сложный процесс, определяющийся рядом факторов. Остановимся на некоторых 
        из них, наиболее специфичных для субкультуры археологов. Важнейшими факторами, 
        влияющими на формирование и степень стабильности репертуара экспедиции, 
        являются: <br>
        <br>
        <strong>1. Состав экспедиции.</strong> Археологические экспедиции, как 
        правило, неоднородны по составу: различны возраст, род занятий, внепрофессиональные 
        интересы ее участников, что позволяет репертуару развиваться во многих 
        направлениях. Экспедиции, традиционно привлекающие к работе практикантов 
        из числа студентов или школьников, имеют больше шансов на обновление и 
        пополнение репертуара. &quot;Текучесть&quot; состава экспедиции — немаловажный 
        фактор, влияющий на репертуар. Чем чаще и сильнее меняется состав (и качественно, 
        и количественно), тем полнее обновляется репертуар, но тем менее он долговечен. 
        В более замкнутых коллективах изменения в репертуаре менее значительны, 
        к новым песням предъявляются большие требования, но однажды принятая песня 
        обретает свое место и, как правило, задерживается надолго. <br>
        <br>
        <strong>2. Песенные пристрастия начальника или центрального поющего звена 
        экспедиции</strong>, которые чаще всего определяют репертуар коллектива 
        и являются основными &quot;ценителями&quot; при отборе новых песен.<br>
        <br>
        <strong>3. Песенная мода конкретной экспедиции</strong>, сложившаяся в 
        течение нескольких лет. Хотя, без сомнения, она чаще всего связана с предыдущим 
        фактором, так как общая направленность репертуара избирается именно &quot;духовным 
        ядром&quot; экспедиции — начальником или &quot;певчим&quot;. <br>
        <strong><br>
        4. Личные песенные привязанности участников экспедиции</strong> (песни 
        того или иного конкретного автора, туристские песни, рок, народные и псевдонародные 
        песни, &quot;блатные&quot; песни и т. п.). Особенно сильно личные песенные 
        привязанности влияют на репертуар экспедиции в тех случаях, когда в экспедицию 
        выезжает какой-то сложившийся коллектив (например, студенты одного курса).<br>
        <br>
        В результате экспедиция принимает в свой репертуар новые песни при условии, 
        что они удовлетворяют общим требованиям (факторы 2 и 4) и не нарушают 
        основной тенденции формирования репертуара (фактор 3), если таковая имеется. 
        С уходом людей или забвением событий, с которыми связано появление песни 
        в репертуаре, она так же безболезненно исчезает из него, как появилась. 
        <br>
        <br>
        <strong>4</strong><br>
        <br>
        Процесс собственно формирования репертуара археологов имеет и некоторые 
        другие особенности. Не все песни из перечисленных ранее групп в равной 
        мере представлены в репертуаре экспедиций, не все они имеют равные шансы 
        попасть в него и занять прочное место. Кроме названных факторов, при отборе 
        важную роль играет мотивно-тематическая направленность песни. Наиболее 
        распространенные в археологическом репертуаре мотивы можно условно распределить 
        по четырем блокам:<br>
        <br>
        <strong>1. Анакреонтические мотивы.</strong> Наиболее актуальным из них 
        (что неудивительно) и в полевых условиях, и на городских встречах становится 
        мотив вина и винопития. Нетрудно заметить, впрочем, что он значительно 
        отличается от соответствующего в мировой анакреонтее, часто преображаясь 
        в тему воинствующего пьянства, где центральная фигура пьяницы и кутилы 
        совсем не напоминает мирного и сдержанного анакреоновского выпивоху. Песни 
        с данными мотивами составляют крупнейший пласт и в большинстве своем повторяются 
        в репертуаре многих экспедиций, что способствует сплочению их участников 
        на общих встречах. Песни, входящие в данный блок, поются, как правило, 
        не отдельно, а целыми циклами, сопровождаясь (иллюстрируясь) &quot;возлияниями&quot;, 
        что подчас изменяет их (песни) до неузнаваемости.<br>
        <br>
        Другие анакреонтические мотивы, например, мотивы ухаживания и флирта (на 
        современном материале — и адюльтера), также присутствуют в археологическом 
        репертуаре в ряде комических песен, которые тоже предпочитают исполнять 
        циклами.<br>
        <br>
        <strong>2. &quot;Туристские&quot; мотивы</strong>, общая идея которых 
        — &quot;мы не в городе&quot;. Показательно в связи с этим присутствие 
        во многих экспедиционных сборниках таких песен, как &quot;Говоришь, чтоб 
        остался я…&quot; Ю. Кукина, &quot;Мне говорят, какой резон…&quot; Б. Вахнюка, 
        &quot;Мы с тобой давно уже не те…&quot; Г. Аделунга и др. При этом туристов 
        в экспедициях, зачастую, &quot;не жалуют&quot;! В сознании исполнителей-слушателей 
        актуализируется все то, что связывает жизнь археолога и туриста (&quot;маргинальность&quot; 
        обстановки, трудности быта, чувство локтя, оппозиционность городу и пр.), 
        а все специфические черты (лавины, перекаты, страховка) или воспринимаются 
        как поэтический прием, дань романтике, или отвергаются. В результате в 
        репертуаре остаются те туристские песни, в которых собственно туристская 
        экзотика минимальна. <br>
        <strong><br>
        3. Историческая тематика.</strong> Песни этого блока в сознании исполнителей-слушателей 
        разделяются на две группы: с одной стороны, это песни о различных исторических 
        событиях или личностях вообще; с другой — песни, соотносимые по тематике 
        непосредственно с местом и объектом раскопок. <br>
        <br>
        <strong>4. Песни, объединенные фигурой маргинального героя.</strong> Чаще 
        всего — это моряк, пират , бродяга, солдат, ковбой. Думается, привилегированное 
        положение таких песен в репертуаре археологов связано все с той же оппозиционностью 
        главного героя окружающему миру. <br>
        <br>
        <strong>5</strong><br>
        <br>
        Что касается функционирования, жизни песен в археологических экспедициях, 
        то следует признать, что, с одной стороны, оно практически полностью подчинено 
        закономерностям, общим для современного городского активного песенного 
        репертуара в целом, и, с другой — законам функционирования песен в субкультуре 
        не строго замкнутого характера. Так, не чуждо репертуару археологов явление 
        циклизации песен, весьма характерное для современной городской песенной 
        культуры. Из особенностей функционирования песен, общих для ряда субкультур, 
        следует назвать заметное противопоставление &quot;своя&quot; — &quot;не 
        своя&quot; песня и вытекающее отсюда ревностное отношение к первым и настороженное 
        — ко вторым. <br>
        <br>
        Помимо тематического признака, являющегося основанием как отбора, так 
        и циклизации, песни в сознании носителей могут складываться в группы по 
        схожести формы, ситуации и особенностей исполнения, культурных коннотаций, 
        связанных с песнями и т. д. <br>
        <br>
        Формальный признак, как основа циклизации, в чистом виде для археологической 
        среды не характерен. Можно наметить лишь отдельные частные особенности 
        формы, влияющие на объединение песен. Уместнее говорить, например, о сходных 
        особенностях исполнения, как объединяющем факторе (помня о том, что некоторые 
        из этих особенностей являются признаками формальными). <br>
        <br>
        Будучи важнейшим средством общения, особенно в полевых условиях, песня 
        выполняет в коллективе интегрирующую функцию. Поэтому неудивительно присутствие 
        большого числа песен, рассчитанных на хоровое исполнение, или приспособленных 
        для него. Конечно, одна и та же песня может по-разному исполняться в разных 
        коллективах в силу сложившейся в них традиции, но такие группы, как &quot;кричалки&quot; 
        или &quot;застольные песни&quot; присутствуют в репертуаре многих экспедиций. 
        Данные группы песен интересны даже не составом, а функцией в репертуаре, 
        причем не только археологическом. В экспедиции они могут исполняться по 
        дороге (например, на раскоп или к лагерю), во время работы, а также (и 
        в основном) в последний или кульминационный момент застолья (посиделок 
        у костра), когда важен сам процесс пения, а песня подходит уже любая. 
        Необходимым условием отбора, следовательно, будет только известность песни 
        всем присутствующим и возможность ее совместного исполнения. Кроме того, 
        объединяющую функцию при исполнении этих песен могут выполнять и невербальные 
        компоненты: ритмичное постукивание кружками, коллективное раскачивание 
        в такт песне, элементы пантомимы, маргинальные выкрики и т. д.<br>
        <br>
        <strong>6</strong><br>
        <br>
        Таким образом, песенный репертуар археологов в целом довольно широк и 
        имеет лишь небольшое количество специфических черт, связанных с профессией. 
        Принципы формирования и развития репертуара подобны тем, которые характерны 
        для других аналогичных субкультур. Специфические особенности могут появляться, 
        впрочем, в каждой отдельной экспедиционной традиции, но обусловлены они 
        не столько родом занятий, сколько спецификой экспедиционной жизни и традициями 
        конкретного коллектива.<br>
