<strong>ВАРИАНТЫ (4)<br>
<br>
1. В эпоху войн, в эпоху кризиса</strong><br>
<br>
В эпоху войн, в эпоху кризисов,<br>
Когда действительность сложна,<br>
У засекреченного физика<br>
Была красавица жена.<br>
Знакомства делала случайные,<br>
Носила кольца и меха,<br>
И к ней подсел брюнет отчаянный<br>
В шашлычной на ВДНХ.<br>
<br>
Сквозь брызги пенного шампанского<br>
И сквозь зернистую икру<br>
Не разглядеть американского<br>
Агента сети ЦРУ.<br>
Забыв инструкции и правила,<br>
И не спросив, кто даже он,<br>
Она и адрес свой оставила,<br>
И свой домашний телефон.<br>
<br>
Вот спит она в его объятиях,<br>
Но никогда не дремлет враг,<br>
Ведь он пришел к ней для изъятия<br>
Секретных формул и бумаг.<br>
Он ищет план объекта энского,<br>
В постель заброшенный субъект,<br>
Ему не надо тела женского,<br>
А нужен ядерный секрет.<br>
<br>
Но всем известные товарищи,<br>
Которым дел до всего,<br>
Его застали в сейфе шарящим,<br>
Причем совсем без ничего.<br>
Они сказали: «Дело ясное,<br>
Кто этот голый господин».<br>
И тут увидела несчастная,<br>
Что не брюнет он, а блондин.<br>
<br>
В эпоху войн, в эпоху кризиса,<br>
Когда действительность сложна,<br>
У засекреченного физика<br>
Должна быть бдительной жена.<br>
<br>
<em>С фонограммы Дмитрия Курилова, CD «В нашу гавань заходили корабли» 
№ 5, «Восток», 2001.</em><br>
