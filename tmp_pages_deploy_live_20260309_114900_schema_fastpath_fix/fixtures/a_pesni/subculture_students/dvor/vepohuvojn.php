<br>
В эпоху войн, в эпоху кризисов,<br>
Когда действительность сложна,<br>
У засекреченного физика<br>
Была красивая жена.<br>
Знакомства делала случайные,<br>
Любила кольца и меха,<br>
И к ней подсел брюнет отчаянный<br>
В шашлычной на ВДНХ.<br>
<br>
Сквозь брызги желтого шампанского<br>
И сквозь зернистую икру<br>
Не разглядеть американского<br>
Агента сети ЦРУ.<br>
Забыв инструкции и правила<br>
И не узнавши, кто есть он,<br>
Она и адрес свой оставила,<br>
И свой домашний телефон.<br>
<br>
Вот спит она в его объятиях,<br>
Забывши, что не дремлет враг,<br>
А он пришел к ней для изъятия<br>
Секретных формул и бумаг.<br>
Он ищет план завода энского,<br>
В постель заброшенный брюнет,<br>
Ему не надо тела женского,<br>
А нужен атомный секрет.<br>
<br>
Но… специальные товарищи,<br>
Что всё узнали про него,<br>
Его застали в сейфе шарящим,<br>
Причем совсем без ничего.<br>
Они сказали даме: - Ясно вам,<br>
Кто этот голый господин?<br>
И тут увидела несчастная,<br>
Что не брюнет он, а блондин.<br>
<br>
В эпоху войн, в эпоху кризисов,<br>
Когда действительность сложна,<br>
У засекреченного физика<br>
Жена быть курвой не должна.<br>
