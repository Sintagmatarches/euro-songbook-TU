	<span class="header"><strong>Я НЕ ЗНАЛ, ЧТО ТЫ ТАКАЯ ДУРА</strong><br></span>
<br>
1. А я не знал, что ты такая дура, <br>
Как корявый пень твоя фигура. <br>
Рожа как лепешка, и еще немножко — <br>
Можно и в зверинец отправлять.<br>
<br>
2. Если б ты была хоть чуть иною, <br>
Как бы хорошо жилось с тобою. <br>
Я бы с песней этой не бродил по свету, <br>
Я бы песнь другую напевал.<br>
<br>
3. К тебе спешу бульваром темным и пустынным, <br>
Пусть ночь глухая меня застанет на пути. <br>
К тебе спешу, и путь не кажется мне длинным, <br>
И я бы мог гораздо больший путь пройти. <br>
<br>
4. Но тебе не быть вовек иною, <br>
Не дождаться от тебя покоя. <br>
От лица-лепешки даже ночью кошки, <br>
Хвост задрав, по городу бегут. <br>
<br>
5. Я не видал тебе подобного урода. <br>
Твою породу я не забуду никогда. <br>
Тебя создав, ошибку сделала природа. <br>
Ты рождена у бога в пьяной голове.<br>
