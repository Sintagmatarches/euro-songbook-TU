<br>
Раз Василий Иваныч пришел на Урал:<br>
Петька рыбу гранатой ловил.<br>
А потом доставал из воды пескарей,<br>
А Чапаев - погибших людей.<br>
Тут Петька вскричал: &quot;Я покину причал.<br>
Но еще не придумал, куда!..&quot;<br>
А Чапай отвечал: &quot;Ты конечно, решай.<br>
Но на гору взгляни, идиот:<br>
<br>
Видишь - там, на горе, принесли пулемет.<br>
Пойди, узнай, для чего, попроси пострелять:<br>
Если вдруг не дадут - возвращайся назад:<br>
Бежать по воде, мотать по воде, тикать по воде со мной!&quot;<br>
<br>
&quot;Да Василий Иваныч, да что пулемет,<br>
Пулеметов я, что ль не видал!<br>
Мы успеем удрать, здесь отлично клюет!&quot;<br>
Петька снова гранату достал.<br>
Онемел Чапаев и в страхе нырнул,<br>
Пытаясь уйти поскорей.<br>
А Петька не глядя гранату швырнул<br>
И полез доставать пескарей...<br>
<br>
Видишь - там, на горе, унесли пулемет.<br>
Петька варит уху, а Чапаев - герой!<br>
Все довольны собой. В котелке - сапоги,<br>
И папаха на дне, папаха на дне, папаха с красной звездой.<br>
<br>
Видишь - там, на горе, ничего не стоит,<br>
А на том ничего не висит ни фига,<br>
А над тем ни фига ничего не горит:<br>
Вот об этом как раз, об этом как раз в этой песне нет ничего.<br>
