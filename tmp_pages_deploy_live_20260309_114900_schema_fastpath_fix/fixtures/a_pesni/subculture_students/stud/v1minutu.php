    Выпьем за хилявших, <br>
    Выпьем за сдававших наобум!  </div>
 <br>
Бывают у студентов <br>
Тяжелые моменты, <br>
Но мы скажем тем, кто упрекнет: <br>
С наше посидите, <br>
С наше позубрите, <br>
С наше посдавайте хоть раз в год!<br>
 <br>
1945 <br>
