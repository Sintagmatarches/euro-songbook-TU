        <span class="text"> <span class="header"><strong>В ПЕРВЫЕ МИНУТЫ</strong><br>
        </span> <br>
        1. В первые минуты<br>
        Бог создал институты,<br>
        И Адам студентом первым был. <br>
        Адам был парень смелый,<br>
        Ухаживал за Евой, <br>
        И бог его стипендии лишил.<br>
        <br>
        У Адама драма, <br>
        Драма у Адама, <br>
        Вызвали Адама в деканат. <br>
        И на землю прямо <br>
        Бог спустил Адама, <br>
        Так о том студенты говорят. <br>
        <br>
        2. От Евы и Адама <br>
        Пошел народ упрямый, <br>
        Нигде не унывающий народ. <br>
        Студент бывает весел <br>
        От сессии до сессии, <br>
        А сессия всего два раза в год.<br>
        <br>
        Что за предрассудки <br>
        Есть три раза в сутки <br>
        И ложиться в теплую кровать?! <br>
        Мы ж без предрассудков <br>
        Едим один раз в сутки, <br>
        А на остальное наплевать.<br>
        <br>
        3. День мы прогуляли,<br>
        Два мы промотали,<br>
        А потом не знали ни бум-бум.<br>
        Так выпьем за гулявших,<br>
        Выпьем за мотавших,<br>
        Сессию сдававших наобум.<br>
        <br>
        <br>
        <img src="v1minuty.gif" width="710" height="763"><br>
        <br>
        <em>Слова – не позднее 1954 года. Исполняется на мотив <a href="../ww2/oficial/korrespond.php">«Песни 
        военных корреспондентов»</a>: стихи К. Симонова («Корреспондентская застольная»), 
        музыка М. Блантера, 1943 год.<br>
        <br>
        Павлинов А.Т., Орлова Т.П. Ни пуха ни пера. Студенческие и туристские 
        песни. СПб.: Композитор (Санкт-Петербург), 2000.</em><br>
        <br>
        <br>
        Считается, что песню написал поэт и драматург Владлен Бахнов (1924-1994), будучи студентом 
        Литературного института имени Горького. Учился он в нем с 1943 по 1949 
        год. По воспоминаниям профессора СПбГЭТУ «ЛЭТИ» Виктора Ивановича Шаповалова (личное электронное письмо на почту сайта от 25.04.2021), он слышал песенку в детстве в 1951-1955 гг., т.е. к началу 1950-х гг. песня была широко известной.<br>
        <br>
        В &quot;Антологии сатиры и юмора России ХХ века. Том 37. Владлен Бахнов&quot; (М.:  Эксмо, 2005) стихотворение датируется 1945 годом:<br>
        <br>
        <span class='bold'>Гимн студентов <br></span>
 <br>
В первые минуты <br>
Бог создал институты, <br>
И Адам студентом первым был. <br>
Он хилял налево, <br>
И гулял он с Евой, <br>
И Бог их общежития лишил. <br>
 <br>
