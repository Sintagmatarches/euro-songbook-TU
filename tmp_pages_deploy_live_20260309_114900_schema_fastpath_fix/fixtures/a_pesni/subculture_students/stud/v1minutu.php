Что за предрассудки <br>
Есть три раза в сутки <br>
И иметь кровать, чтоб ночевать, <br>
Мы в своем рассудке — <br>
Мы едим раз в сутки, <br>
А на остальное наплевать! <br>
 <br>
    <div class='refren'>День мы прогуляем, <br>
