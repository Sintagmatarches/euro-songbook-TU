 <br>
От Евы и Адама <br>
Пошел народ упрямый, <br>
Пошел неунывающий народ: <br>
Студент бывает весел <br>
От сессии до сессии, <br>
А сессии всего два раза в год! <br>
 <br>
    <div class='refren'>День мы прогуляем, <br>
