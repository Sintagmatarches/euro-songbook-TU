        <strong>ВАРИАНТЫ (6)<br>
        <br>
        1. Студенческая</strong><br>
        <br>
        В первые минуты<br>
        Бог создал институты.<br>
        И Адам студентом <br>
        первым был.<br>
        Он ходил на небо<br>
        И гулял там с Евой.<br>
        Бог его стипендии лишил.<br>
        <br>
        От Евы и Адама<br>
        Пошел народ упрямый,<br>
        Пошел <br>
        неунывающий народ.<br>
        Живут студенты весело<br>
        От сессии <br>
        до сессии,<br>
        А сессии всего два раза в год.<br>
        <br>
        <em>Прислана Тамарой Николаевной Смирновой из Ярославля<br>
        <br>
        Ярославская областная газета <a href="http://www.goldring.ru" target="_blank">«Золотое 
        кольцо»</a>, 02.10.2002, рубрика «Песни нашего двора».</em><br>
        <br>
        <strong><br>
        2. Выпьем за гуляющих</strong><br>
        <br>
        В первые минуты<br>
        Бог создал институты,<br>
        И Адам его студентом первым был. <br>
        Он ничего не делал, <br>
        Ухаживал за Евой, <br>
        И Бог его стипендии лишил.<br>
        <br>
        Адам не растерялся, <br>
        На станцию подался, <br>
        На станции вагоны разгружал. <br>
        Ах, Ева, Ева, Евочка, <br>
        Хорошая ты девочка, <br>
        Но Бог меня за это наказал. <br>
        <br>
        У Адама драма, драма у Адама — <br>
        Вызвали Адама <br>
        В божий деканат. <br>
        И на землю прямо <br>
        Сбросили Адама... <br>
        Так пошли студенты, говорят. <br>
        <br>
        От Евы и Адама <br>
        Пошел народ упрямый, <br>
        Лихой, неунывающий народ. <br>
        От сессии до сессии <br>
        Живут студенты весело, <br>
        А сессия всего два раза в год. <br>
        <br>
        Что за предрассудки — <br>
        Есть три раза в сутки <br>
        И ложиться в чистую кровать?! <br>
        У нас свои рассудки, <br>
        Едим один раз в сутки, <br>
        А на остальное наплевать.<br>
        <br>
        Когда ж наступит лето, <br>
        Пойдем бродить по свету <br>
        В драных кедах, рваных сапогах. <br>
        Встретим мы озера, <br>
        И леса, и горы, <br>
        А рассветы встретим на ногах. <br>
        <br>
        Беда в том небольшая, <br>
        Что тумбочка пустая. <br>
        Подушка а ни пуха ни пера. <br>
        Так выпьем за гуляющих, <br>
        И ни черта не знающих, <br>
        Ведь сессия — счастливая пора! <br>
        <br>
        <em>Песни нашего двора / Авт.-сост. Н.В. Белов. Минск: Современный литератор, 
        2003.  (Золотая коллекция). </em><br>
        <br>
        <br>
        <strong>3. В первые минуты…</strong><br>
        <br>
        В первые минуты <br>
        Бог создал институты, <br>
        И Адам студентом первым был. <br>
        Он ничего не делал, <br>
        Ухаживал за Евой, <br>
        И Бог его стипендии лишил. <br>
        <br>
        У Адама драма, <br>
        Вызвали Адама <br>
        На расправу в Божий деканат, <br>
        И на землю прямо <br>
        Сбросили Адама. <br>
        Так пошли студенты, говорят. <br>
        <br>
        От Евы и Адама <br>
        Пошел народ упрямый, <br>
        Лихой, неунывающий народ. <br>
        От сессии до сессии <br>
        Живут студенты весело, <br>
        А сессия всего два раза в год. <br>
        <br>
        Что за предрассудки - <br>
        Есть три раза в сутки <br>
        И ложиться в чистую кровать?! <br>
        У нас свои рассудки, <br>
        Едим один раз в сутки, <br>
        А на остальное наплевать. <br>
        <br>
        Когда ж наступит лето, <br>
        Пойдем бродить по свету <br>
        В драных кедах, рваных сапогах. <br>
        Встретим мы озера,<br>
        И леса, и горы, <br>
        А рассветы встретим на ногах.<br>
        <br>
        День мы прогуляли, <br>
        Ночь мы провлюблялись, <br>
        А наутро снова ни бум-бум. <br>
        Так выпьем за гулявших, <br>
        За ни черта не знавших, <br>
        За сессии сдававших наобум! <br>
        <br>
        А есть, представьте, люди, <br>
        Которые нас судят: <br>
        - Ну что за несознательный народ! <br>
        С наше поучите, <br>
        С наше позубрите, <br>
        С наше походите на зачет!!! <br>
        <br>
        <em>А я не уберу чемоданчик! Песни студенческие, школьные, дворовые / 
        Сост. Марина Баранова. М.: Эксмо, 2006.</em><br>
        <br>
        <br>
        <strong>4. От сессии до сессии</strong><br>
        <br>
        С первых же минут<br>
        Бог создал институт,<br>
        Адам его студентом первым был.<br>
        Он ничего не делал,<br>
        Ухаживал за Евой,<br>
        И Бог его стипендии лишил.<br>
        <br>
        Ах, какая драма -<br>
        Вызвали Адама,<br>
        За прогулы в божий деканат.<br>
        И на землю прямо<br>
        Скинули Адама -<br>
        Так пошли студенты, говорят.<br>
        <br>
        Адам не растерялся -<br>
        На станцию подался<br>
        С картошкою вагоны разгружать.<br>
        Ах, Ева, Ева, Евушка,<br>
        Хорошая ты девушка,<br>
        Но за тебя приходится страдать.<br>
        <br>
        От Евы и Адама<br>
        Пошел народ упрямый,<br>
        Живой, неунывающий народ.<br>
        От сессии до сессии<br>
        Живут студенты весело,<br>
        А сессия всего два раза в год.<br>
        <br>
        И день мы прогуляем<br>
        И два мы прогуляем<br>
        На третий день не знаем ни бум-бум.<br>
        Так выпьем за гулявших,<br>
        За ничего не знавших,<br>
        За сессию сдававших наобум.<br>
