	  Филипп Шкулев<br>
<br>Что ты кайзер глядишь на Россию<br>
	  Погляди, что творится вокруг,<br>
	  Ох, придется сломать тебе шею,<br>
	  Разнесут твою армию в пух.<br>
	<br>И зачем ты задумал жестокий<br>
	  Натворить столько горя и бед?<br>
	  Пролил крови и слез ты потоки,<br>
	  Ужаснулся от них белый свет.<br>
	<br>На тебя все глядят как на хама,<br>
	  Все с презреньем бегут тебя прочь,<br>
	  Ведь тобою творимая драма,<br>
	  так ужасна и мрачна как ночь.<br>
	<br>Захотел ты позорною войною<br>
	  Все народы себе покорить.<br>
	  И над миром, ноукой святою,<br>
	  Черный крест навсегда водрузить.<br>
	<br>Но призрел твою гнусную волю<br>
	  Благородный великий народ.<br>
	  Он геройски за лучшую долю<br>
	  Биться с вражеской силой идет.<br>
	<br>И уложит ее он в могилу.<br>
	  Проклянешь ты свой избранный путь,<br>
	  Пожалеешь погибшую силу,<br>
	  Но былого тебе не вернуть.<br>
	<br>
	Не гляди ты как зло на народы,<br>
	  Лучше трон свой спасти поспеши,<br>
	  Пощади свой народ от невзгоды<br>
	  И кровавый позор заглуши.<br>
	<br>Не добыть тебе то, что желалось,<br>
	  А ведь русские идут стеной.<br>
