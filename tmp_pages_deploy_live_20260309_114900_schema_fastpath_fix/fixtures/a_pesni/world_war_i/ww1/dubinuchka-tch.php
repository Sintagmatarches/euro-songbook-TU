Фалеев Николай Иванович</strong> (1872–1941) - русский поэт, писатель, драматург, публицист, военный юрист, общественный и политический деятель. Окончил московское Александровское военное училище и Военно-юридическую академию в Петербурге. Был одним из адвокатов на процессе Бориса Савинкова (1906, Севастополь) и процессе над участниками восстания на крейсере &quot;Очаков&quot;. Преподавал право в петербургском Императорском лесном институте. Участник Первой мировой войны. Летом 1917 года по приглашению эсеров стал товарищем министра земледелия Временного правительства. После Октябрьского переворота включен в коллегию Наркомата земледелия, участвовал во внедрении продразверстки. С 1923 года - ответственный редактор «Известий ВЦИК», после инсульта в 1937 году ушел на пенсию. <br>
