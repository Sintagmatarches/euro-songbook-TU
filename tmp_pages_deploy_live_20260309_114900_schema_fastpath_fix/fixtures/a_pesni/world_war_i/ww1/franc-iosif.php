<br>Франц-Иосиф приуныл,<br>
	  Города сдавая;<br>
	  А Вильгельм ус опустил,<br>
	  Дурака ломая.<br>
	<br>Стал он русским угрожать<br>
	  Армией большою,<br>
	  Но Россию трудно взять<br>
	  С глупой головою.<br>
	<br>И где только в городах<br>
	  Швабы побывали,<br>
	  Наводили всюду страх,<br>
	  Сильно воровали.<br>
	<br>Церкви и костелы жгли,<br>
	  Зданья разрушали;<br>
	  Издевались как могли,<br>
	  В жителей стреляли.<br>
	<br>И картошку всю в полях<br>
	  Вырыли машиной;<br>
	  Разоряли в деревнях<br>
	  Мужичков скотиной.<br>
	<br>Все, что было у селян,<br>
	  Немцы расхищали,<br>
	  Наносили им изъян,<br>
	  Женщин обижали.<br>
	<br>У крестьян домашних птиц<br>
	  Швабы отбирали,<br>
	  А молоденьких девиц<br>
	  В Пруссию угнали.<br>
	<br>Черт всех немцев обхватил<br>
	  Пакостить повсюду;<br>
	  Сам Вильгельм, где только был,<br>
	  Стал противен люду.<br>
	<br>Так для зверств дан немцам ум,<br>
	  Швабы ошалели;<br>
	  Разрывных же пуль «дум-дум»,<br>
	  В битвах не жалели.<br>
	<br>Начал шваб вдруг отступать<br>
