	<span class="header"><strong>ИЗ ТАЙГИ, ТАЙГИ ДРЕМУЧЕЙ</strong><br></span>
 <br>
  Музыка неизвестного автора<br>
  Слова Владимира Гиляровского<br>
  <br>
