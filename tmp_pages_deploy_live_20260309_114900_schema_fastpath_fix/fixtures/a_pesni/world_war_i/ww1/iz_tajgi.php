  <br>
  <em>Неизвестный источник</em><br>
  <br>
  <br>
  Песня на стихотворение писателя  Владимира Гиляровского. Написано не позднее 1915 года, так как есть публикации этого года (например: Солдатские военные песни Великой Отечественной войны 1914-1915 гг. Собрал В. Крылов. Харбин: Типография Заам. Окр. Погр. Стр. 1915).<br>
  <br>
  На эту же мелодию поются две знаменитые песни Гражданской войны: дальневосточная 
  партизанская <a href="../grvojna/kr/podolinam.php">&quot;По долинам, по загорьям&quot;</a> 
  Петра Парфенова (более известна в обработке Сергея Алымова как &quot;По долинам 
  и по взгорьям&quot;) и белогвардейский <a href="../grvojna/bel/marchdrozd.php">&quot;Марш 
  Дроздовцев&quot;</a>. По словам Парфенова, он написал &quot;По долинам, по загорьям&quot; 
  на мотив своих же песен &quot;На Сучане&quot;, сочиненных в июле 1914 года:<br>
  <br>
  <em>По долинам и по взгорьям<br>
  Целый месяц я бродил,<br>
  Был на реках и на взморьях<br>
  Не жалея юных сил...</em><br>
  <br>
  О существовании дореволюционной песни дальневосточных охотников &quot;По долинам, 
  по загорьям&quot; с той же мелодией утверждается и в буклете к CD «Тернистый 
  путь борьбы и муки. Песни Белого движения и Русского Зарубежья», СПб., IMLab, 
  2004, стр. 13). Так что она, возможно, и является предшественницей всех упомянутых 
  песен. Однако не ясно, сам ли Парфенов сочинил ее мотив или в свою очередь где-то 
  заимствовал.<br>
  <br>
  <br>
  В тексте &quot;Марша сибирских стрелков&quot; есть явные параллели с 
  казачьими песнями, напр. с кубанской <a href="../kazaki/kuban/pkarabin.htm">&quot;Песней 
  карабинеров&quot;</a> (опять же, непонятно, в какой песне эти строки появились раньше):<br>
  <br>
  <em>Карабинер усы крутит,<br>
  Карабинер ладно шутит.<br>
  Сдвинет шапку набекрень, -<br>
  Будет драться ночь и день.</em> <br>
  <br>
  <br>
  Первый куплет песни Гиляровского с небольшими изменениями перешел в партизанскую 
  песню<a href="../grvojna/kr/izzalesulesu.php"> &quot;Из-за лесу, лесу, лесу&quot;</a>, 
  посвященную командиру Восточно-Забайкальским фронтом П.Н. Журавлеву (исполнялась 
  на мотив <a href="../popular20/izzaostrova.php">&quot;Из-за острова на стрежень&quot;</a>):<br>
  <br>
  <em>Из-за лесу, лесу, лесу<br>
  От Амура, от реки<br>
  Шли могучей, грозной тучей,<br>
  Шли на бой большевики.</em><br>
