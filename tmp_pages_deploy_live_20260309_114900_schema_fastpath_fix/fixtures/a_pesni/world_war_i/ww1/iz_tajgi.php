  И сибирские снега. <br>
  <br>
  Ни усталости, ни страха; <br>
  Бьются ночь и бьются день, <br>
  Только серая папаха <br>
  Лихо сбита набекрень. <br>
  <br>
  Эх, Сибирь, страна родная, <br>
