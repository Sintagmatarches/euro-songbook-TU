	<span class="header"><strong>КАК ВИЛЯ БРАЛ ВАРШАВУ </strong></span><br>
	  Песня сложена после первого нападения немцев на Варшаву в октябре-ноябре 1914 г.<br>
	<br>Закрутил Виля усы<br>
	  В виде швабской колбасы.<br>
	  <div style="margin-left:32px">Это правда, это правда,<br>
	  В виде швабской колбасы!</div>
      <br>
	  Пьет пивишко, улыбается,<br>
	  Взять Варшаву собирается.<br>
	<br>Эх, для нас ли, для вояк,<br>
	  Взять Варшаву же пустяк!<br>
	<br>Шлет гонца Виля с приказом -<br>
	  Взять Варшаву одним разом!<br>
	<br>И пошла немецка рать<br>
	  Русскую Варшаву брать.<br>
	<br>Бьются день, бьются два, -<br>
	  Все ж Варшава не видна.<br>
	<br>Но на деле друго вышло:<br>
	  Стало немцу — ух, как кисло!<br>
	<br>Видит Виля — дела плохи,<br>
	  Завздызал он: охи, охи!<br>
	<br>Стал он думать да гадать,<br>
	  Как назад-то хоть удрать.<br>
	<br>Русски бьют без передышки -<br>
	  Всему войску будет крышка.<br>
	<br>И пустилась прусска рать,<br>
	  Что есть силы, удирать!<br>
	<br>От Варшавы без оглядки,<br>
	  Засверкали только пятки.<br>
	<br>Мы же маху не давали,<br>
	  Сзади жару поддавали.<br>
	<br>Подбодряли, веселили,<br>
	  В них из пушечек палили.<br>
	<br>И несметна прусска рать<br>
	  Улеглась навеки спать!<br>
	<br>Из живых кто и ушел,<br>
	  Без скулы домой пришел!<br>
	<br>Долго, долго будешь знать,<br>
	  Как Варшаву русску брать!<br>
