Осовец - приграничная крепость на западной границе Российской империи, прикрывавшая направления Берлин - Петербург и Вена - Петербург. Современный городок Осовец-Крепость в Польше (Osowiec-Twierdza) в 42 км от Белостока. С сентября 1914 по август 1915 года Осовец был трижды безуспешно атакован германскими войсками. 18-22 августа в связи с общим отступлением русских войск на Западном фронте крепость была взорвана и покинута.<br>
