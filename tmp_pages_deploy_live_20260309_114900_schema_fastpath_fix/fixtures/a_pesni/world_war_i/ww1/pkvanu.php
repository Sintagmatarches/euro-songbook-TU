	<span class="header"><strong>ПОХОД К ВАНУ</strong></span><br>
	<br>Урядник Иван Прийма<br>
	<br>Вспомним, братцы, мы то время,<br>
	  Когда вел нас генерал<br>
	  Чрез высокий и холодный<br>
	  Тапаризский перевал.<br>
	<br>Вспомним, братцы, как ходили<br>
	  По колено мы в снегу,<br>
	  Как коней в руках водили,<br>
	  Как на встречу шли врагу,<br>
	<br>Где снега почти не тают.<br>
	  Там, где вечный дом зимы,<br>
	  Где орлы одни летают,<br>
	  Даже там бывали мы.<br>
	<br>Вон вдали чернеют бурки,<br>
	  И белеют башлыки...<br>
	  То не Курды и не Турки,<br>
	  То кубанцы казаки.<br>
	<br>Через камни, через горы,<br>
	  Чрез глубокие снега<br>
	  Вот разъезды и дозоры<br>
	  Едут, щупают врага.<br>
	<br>Много дней они в походе,<br>
	  Курдам места не дают;<br>
	  Сухари уж на исходе,<br>
	  Да и кони устают.<br>
	<br>Казаки не унывают,<br>
	  Едут ночью, едут днем:<br>
	  Каждый день почти бывают<br>
	  Под винтовочным огнем.<br>
	<br>Пик кубанцы не имеют,<br>
	  И не нужно им штыков,<br>
	  И без них они умеют<br>
	  Бить османов, как быков.<br>
	<br>Много раз мы их побили,<br>
	  Стало дело их «яман»,<br>
	  наконец, мы все вступили<br>
	  В их великий город Ван.<br>
	<br>Никогда не позабудем<br>
	  Этот памятный нам год;<br>
	  Вспоминать мы долго будем<br>
	  К Вану городу поход.<br>
	<br>
