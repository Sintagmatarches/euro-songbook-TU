<br>Под крепким стенам Перемышля,<br>
  С кровавых австрийских полей<br>
  Калека-солдат истомленный<br>
  Плетется к деревне своей.<br>
<br>Он в битве участвовал часто,<br>
  Врагов поднимал на штыки.<br>
  Лишился он волею рока<br>
  Кормилицы - правой руки.<br>
<br>И вот он идет и мечтает:<br>
  Вот скоро родительский дом,<br>
  А ветер шутливо играет<br>
  Шинелью, пустым рукавом.<br>
<br>И вот затемнели овины,<br>
  Хибары деревни родной…<br>
  <br>
  Он видит родную избенку,<br>
  За ней небольшой огород.<br>
  В избе заколочены окна.<br>
  Наверно, никто не живет.<br>
<br>«Соседушки, люди, скажите,<br>
  Ответу я каждому рад:<br>
  Где мать, дорогая супруга,<br>
  Сестра и единственный брат?»<br>
<br>«Жену твою барин проезжий<br>
  В столицу увез за собой;<br>
  А мать твоя с горя-кручины<br>
  В могилу ушла на покой;<br>
<br>Сестра ваша там, в лазарете,<br>
  Дежурит у коек, не спит,<br>
  А брат твой, лихой доброволец,<br>
  В бою под Варшавой убит».<br>
<br>Ни слова солдат не ответил,<br>
  Лишь поднял он к небу глаза;<br>
  Была в них великая клятва<br>
  И будущей мести гроза.<br>
