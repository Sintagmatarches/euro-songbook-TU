	Песенка времен Первой мировой войны композитора Виктора Гавриловича Пергамента на стихи правоведа Николая Фалеева, публиковавшегося под псевдонимом &quot;Чуж-Чуженин&quot;, о новом герое городской публики - прапорщике. См. также песню <a href="praporjun.php">&quot;Прапорщик юный&quot;</a>.<br>
