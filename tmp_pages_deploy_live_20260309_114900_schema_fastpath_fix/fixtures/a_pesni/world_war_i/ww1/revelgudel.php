	  От вражьих выстрелов кругом.<br>
	  Он на коне в блестящей каске<br>
	  Сидел с сигарою верхом.<br>
	<br>Его чело терзали думы:<br>
	  Возьмет ли он Европу в плен?<br>
	  Дойдет ли он до Петрограда,<br>
	  И до Московских белых стен?<br>
	<br>Кругом в огне пылали села,<br>
	  Горели также города.<br>
