	  Творили зверства без стыда.<br>
	<br>Громили храмы и соборы,<br>
	  Терзали в злобе бедных жен,<br>
	  Малюток, старцев убивали...<br>
	  Кругом носился плач и стон.<br>
	<br>«Зачем творишь ты эти зверства?».<br>
	  Вдруг ветер кайзеру шепнул.<br>
	  Усы его зашевелились<br>
	  И он порывисто вздохнул:<br>
	<br>«Я вижу войско погибает<br>
