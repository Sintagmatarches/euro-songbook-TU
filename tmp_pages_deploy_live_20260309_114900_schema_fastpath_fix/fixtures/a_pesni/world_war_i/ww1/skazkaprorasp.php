	<span class="header"><strong>СКАЗКА ПРО РАСПУТИНА</strong><br></span><br>
	В нашем ложном бывшем царстве,<br>
	  В министерском государстве<br>
	  Николай был царь с царицей<br>
	  По прозванию Алисой.<br>
	  Пятерых имел детей:<br>
	  Четырех-то дочерей,<br>
	  Да имел еще сына одного -<br>
	  Только он не от него.<br>
	  Стали дочери рождаться,<br>
	  Царь стал сильно сомневаться:<br>
	  Пропадет мое наследство!<br>
	  Вот и стал искать он средства,<br>
	  Чтобы сына получить<br>
	  И престол ему вручить.<br>
	  Дал министрам объявление,<br>
	  Чтоб нашли такого гения,<br>
	  Чтобы мог сразу определить,<br>
	  Что жене сына родить.<br>
	  - Кто найдется из вас такой,<br>
	  Пусть к жене идет в покой.<br>
	  Если ж только ошибется,<br>
	  Со своей жизнью расстается!<br>
	  Как ни зарились министры,<br>
	  Как ни были очень быстры,<br>
	  Но по строгом указу<br>
	  Отказалися все сразу.<br>
	  Штюрмер как-то раз, было, взялся,<br>
	  Да потом и побоялся.<br>
	  Был у ней, как у сестрицы,<br>
	  Мясоедов — друг царицы,<br>
	  И хотя была охота,<br>
	  Но боялся эшафота.<br>
	  Все хотели отличиться,<br>
	  Да ну-ка дочь опять родится?!<br>
	  Вопрос сложный, не простой -<br>
	  Надо жертвовать собой.<br>
	  Все прошли они село,<br>
	  Не нашедши никого<br>
	  Оказать царю услугу -<br>
	  Полюбить его супругу.<br>
	  Царь стоял все на одном:<br>
	  - Расстреляю я весь дом!<br>
	  Если сына не добуду,<br>
	  То жену свою забуду.<br>
	  Тут министры сообразили<br>
	  И царю все предложили,<br>
	  Чтоб из подданных найти,<br>
	  А свою бы жизнь спасти.<br>
	  И нашелся толстопузый<br>
	  И дородный парень, дюжий,<br>
	  Трутень,<br>
	 <div style="margin-left:64px"> конокрад</div>
	  Гришка Распутин.<br>
	  Тут Алиса прибежала,<br>
	  Вся краснела и дрожала,<br>
	  Но дрожала не от испуга,<br>
	  А от радости для друга.<br>
	  Царь сказал:<br>
	  - Ну, так и быть,<br>
	  А чтоб сына мне родить.<br>
	  Если же родится дочь,<br>
	  То повешу в эту ночь.<br>
	  - Буду, царь, для вас стараться;<br>
	  Буду потом обливаться, -<br>
	  Молвил Распутин так царю,<br>
	  Низко кланяясь ему.<br>
	  - Коль клянешься ты стараться,<br>
	  Можешь к делу приниматься.<br>
	  Только сильно не спешить,<br>
	  Чтоб людей не насмешить!<br>
	  Во дворце Распутин остался.<br>
	  Он с Алисой там спознался.<br>
	  Был он в роскоши такой,<br>
	  Не чаял смерти над собой.<br>
	  Все его там уважали,<br>
	  Языком пятки лизали.<br>
	  А для Алисы для самой<br>
	  Распутин сделался святой.<br>
	  Что проделывали оне,<br>
	  То известно сатане.<br>
	  А нам трудно вспоминать,<br>
	  И нехватит сил писать.<br>
	  Год прошел, тут сын родился.<br>
	  Царь запрыгал, веселился<br>
	  И Распутина обнял.<br>
	  - Благодарю, Гриша, - сказал, -<br>
	  За твои труды, Григорий,<br>
	  Ты у меня будешь при дворе,<br>
	  Будем дружны мы с тобой,<br>
	  Ты у меня будешь большой!<br>
	  Сын все рос,<br>
	  И царь покоен,<br>
	  Только одним лишь был расстроен,<br>
	  Что народ стал волноваться,<br>
	  Земли и воли добиваться.<br>
	  Царь задумался: как быть<br>
	  И людей всех усмирить?<br>
	  Он собрал своих рабов,<br>
	  Всех министров и попов:<br>
	  - Скоро все мы пропадем,<br>
	  Если средства не найдем,<br>
	  Как отнесся нам к народу<br>
	  И не дать бы им свободу.<br>
	  Тут министр бывший Столыпин<br>
	  План было выкинул великий,<br>
	  Землю стал закрепощать;<br>
	  Попы нач<strong>а</strong>ли причитать<br>
	  И напутствовать людей:<br>
	  - Платите больше податей,<br>
	  Царя-батюшку любите,<br>
	  И господ вы не громите,<br>
	  Землю вы не забирайте,<br>
	  Она их. Вы это знайте!<br>
	  Коли силой отберете,<br>
	  На том свете в ад пойдете!<br>
	  - Коль голодные мы будем -<br>
	  Землю мы от вас добудем!<br>
	  Хоть и к чорту в ад пойдем<br>
	  И ему хвост оторвем!<br>
	  Слышит речи царь такие -<br>
	  Очень грозные, лихие<br>
	  От своих подданных людей -<br>
	  Не боятся уж чертей!<br>
	  «Надо иначе поступить<br>
	  И войну теперь открыть.<br>
	  Мы войной людей осадим,<br>
	  С остальными уж поладим!»<br>
	  Шлет Вильгельму царь пакет,<br>
	  У него просит совет:<br>
	  «Отпиши, дружок, ты мне<br>
	  Нет ли средства у тебе<br>
	  Моему горю помочь,<br>
	  И людей моих побить?..<br>
	  А то много развелось,<br>
	  Жить мне мало осталось,<br>
	  Скоро свергнут они меня<br>
	  И доберутся до тебя!<br>
	  А миллионов пять побьем<br>
	  Еще с полвека проживем!»<br>
	  Так цари войну открыли,<br>
	  А попы заговорили:<br>
	  - На войне кто и умрет,<br>
	  В рай небесный попадет!<br>
	  Беззакония творя,<br>
	  Поп поддерживал царя.<br>
	  Эта жирная порода -<br>
	  Друг царя, а не народа!..<br>
