	  С похода думал отдохнуть,<br>
	  Но не успел он растянуться,<br>
	  Пяти минут не мог соснуть.<br>
	  - Вставайте, братцы, поднимайтесь! -<br>
	  Фельдфебель зычно прокричал.<br>
	  Какая может быть тревога:<br>
	  Сегодня только полк наш стал?<br>
	  Солдату некуда деваться,<br>
	  И полк тот жаркий ой начал.<br>
	  Солдат, устав в походе раньше,<br>
	  В бою том тоже уставал.<br>
	  И тут на штык он вражий острый,<br>
	  С врагом сражаяся, летит.<br>
	  И пал солдат в бою бесславно,<br>
	  Не зная: кем, за что убит...<br>
	  И спросят дети у мамаши:<br>
	  - Скажи, где папка наш родной?<br>
	  А мать ответит со слезами:<br>
	  - Он там убит в земле чужой.<br>
	  Убит за то, что был солдатом,<br>
	  Буржуев счастье охранял.<br>
