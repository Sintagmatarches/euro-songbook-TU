        вероятно, в середине 1930-х гг.: к этому времени относятся его записи 
        на пластинку в исполнении оркестра Лукьеси. Оригинальное название - &quot;Espuma 
        de Champagne&quot; (&quot;Пена шампанского&quot;). Сочинена не ранее 1936 г., так как в СССР запрет на новогоднюю елку был снят в декабре 1935 г. Ниже приведен вариант с реалиями Великой Отечественной войны - если песня действительно сочинена не позднее нее, то фраза &quot;порядки новые&quot; не связана с ужесточением лагерных порядков на манер концлагерей, в том числе  запрете свободного перемещения внутри лагеря, что произошло вскоре после войны, как и объявление Нового года выходным (с 1947 г.).<br>
