        <br>
        <em>Из кн. В. Ларионова &quot;Последние юнкера&quot;. (Виктор Александрович 
        Ларионов – артиллерист-марковец, офицер производства 1918 года, впоследствии 
        член Р.О.В.С., организатор и участник терракта 7 июня 1927 года в Ленинграде 
        - налет на заседание Агитпропотдела Ленинградской коммуны, Мойка № 59 
        - всего 26 раненых).<br>
        </em><br>
        <br>
        Обычно поется: <em>&quot;Слышали деды, война началася&quot;</em>. Песня 
        Добровольческой армии, переделка популярной песни времен Первой мировой 
        войны (см. <a href="../../ww1/sldedy.php">«Слыхали, деды, война 
        началася»</a>). Мелодия куплетов заимствована у романса <a href="../../romans/belakacii.php">«Белой 
        акации гроздья душистые»</a> &lt;1902&gt;, припев - мелодия гусарской 
        мазурки. Известно множество вариантов под разными заглавиями: &quot;Добровольческая 
        песня&quot;, &quot;Слышали деды, война началася&quot;, &quot;Мы смело 
        в бой пойдем!&quot;. Наибольшее распространение песня имела у марковцев 
        (как раз данный вариант - один из марковских). <br>
        <br>
        Там же в книжке Ларионова приводится такая сатирическая переделка «Добровольческой 
        песни»: <br>
        <br>
        Смело мы в бой пойдём,<br>
        А я останусь…<br>
        С частью хозяйственной<br>
        Я не расстанусь…<br>
        <br>
        <br>
        Припев мог исполняться и в других, более живописных редакциях: <br>
        <br>
        Мы смело в бой пойдем за Русь Святую <br>
        И всех жидов побьем – сволочь такую…<br>
        <br>
        Мы смело в бой пойдем за Русь Святую <br>
        Большевиков побьем – сволочь такую…<br>
        <br>
        <br>
        Генерал Петр Краснов изображает исполнение песни Добровольческой армией, 
        выступившей в феврале 1918 года в 1-й Кубанский &quot;Ледяной&quot; поход, 
        в романе &quot;От двуглавого орла к красному знамени&quot; (1921):<br>
        <br>
        Вместе пойдем мы<br>
        За Русь святую!<br>
        И все прольем мы<br>
        Кровь молодую!<br>
        Близко окопы...<br>
        Трещат пулеметы...<br>
        <br>
        Тот же самый фрагмент песни он приводит и в другом эпизоде романа: поет 
        его в марте 1920 года семилетняя девочка на Принцевых островах в Мраморном 
        море, куда ей удалось эвакуироватся при Новороссийской катастрофе Деникина. 
        Песне ее научил офицер в Новороссийске.<br>
        <br>
        Дроздовец Дмитрий Пронин вспоминал, что эту песню пели в Добровольческой армии на всем протяжении войны и цитирует два куплета (<em>Дмитрий Пронин. Записки дроздовца-артиллериста / Пронин Д., Александровский Г., Ребиков Н. Седьмая гаубичная. 1918-1920. Нью-Йорк, 1960. С. 101):</em><br>
<br>
Мы смело в бой пойдем<br>
За Русь Святую<br>
И, как один, прольем<br>
Кровь молодую.<br>
<br>
Вот и окопы,<br>
Трещат пулеметы,<br>
Но их не боятся<br>
Дроздовского (Корнилова) роты.<br>
