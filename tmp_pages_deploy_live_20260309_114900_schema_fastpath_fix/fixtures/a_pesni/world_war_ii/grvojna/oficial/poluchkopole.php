        <br>
        В интернете встречается аналогичная <a href="../bel/pchkuro.php">белогвардейская 
        песня</a> - под названием &quot;Песня партизан-шкуровцев&quot;.
        В поддержку ее подлинности выдвигают тот факт, что автор музыки песни 
        композитор Лев Книппер, брат киноактрисы Ольги Чеховой, в годы Гражданской 
        сражался за белых - пошел добровольцем, служил артиллерийским офицером 
        у Врангеля, затем оказался в эмиграции, но вернулся в Россию. Чисто теоретически, 
        он мог написать белую &quot;Полюшко-поле&quot; в годы Гражданской или 
        в эмиграции. Однако никаких доказательств этой версии нет. <br>
        <br>
        Есть и <a href="../bel-emigr/poluchko-emigr.php">реальный эмигрантский  вариант</a> &quot;Полюшко-поле&quot;, входивший в  репертуар 
        Хора донских казаков под управлением Сергея Жарова. Хор был 
        создан в 1921 году в лагере Чилингир в Турции и разделил 
        дальнейшую судьбу казаков-эмигрантов:- остров Лемнос, Болгария, а с началом Второй Мировой - США. В этой версии поется &quot;Русской армии герои&quot;, а при издании указывалось - &quot;аранжировка Книппера&quot;. <br>
        <br>
        Еще одна версия &quot;Полюшко-поле&quot; входила в репертуар певца Русского Зарубежья Ивана Реброва. Ребров начинал русскую исполнительскую карьеру в Черноморском казачьем хоре, был хорошо знаком с Хором донских казаков Жарова и перенимал у него исполнительские приемы. Возможно, этот вариант написан специально для Реброва: <br>
