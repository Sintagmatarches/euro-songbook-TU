        <span class="header"><strong>МОНТЕ-КАССИНО</strong><br>
        </span> <strong><br>
        <br>
        Зигмунт Богуш-Шишко<br>
        <br>
        ПОСЛЕСЛОВИЕ К КНИГЕ МЕЛЬХИОРА ВАНЬКОВИЧА «ЭТО БЫЛО ПОД МОНТЕ-КАССИНО» 
        (ЛОНДОН, 1954)</strong><br>
        <br>
        Человеческая драма, которая называется битвой, столь сложна и многогранна, 
        что даже самый способный и самый проницательный наблюдатель не может охватить 
        ее полностью и описать исчерпывающе. Самое трудное — это уловить и понять 
        сущность кризиса, который рождается в момент начала боя, нарастает по 
        мере его хода и затем часто предопределяет его исход.<br>
        <br>
        Истоки кризиса неизменно коренятся в глубинах человеческого духа, а формы 
        кризиса столь же различны, как различен духовный облик участников драмы. 
        По-своему переживает и видит битву военный корреспондент, ответственный 
        лишь за себя самого, по-своему — рядовой, связанный общей задачей с товарищами, 
        и по-своему, совсем иначе, — командиры всех уровней снизу доверху.<br>
        <br>
        Никогда нельзя предвидеть, что станет причиной кризиса и какую форму он 
        обретет. Картина битвы за Монте-Кассино, начертанная пером Мельхиора Ваньковича, 
        поражает нас ужасом кризиса, вызванного невероятно тяжкими потерями и 
        крушением первого натиска на немецкие позиции. Картина, однако, неполная 
        и односторонняя. Этому не приходится удивляться, потому что во время битвы 
        видятся только внутренние проявления кризиса, а после битвы участники 
        неохотно говорят о сущностном его содержании.<br>
        <br>
        Кризис в битве за Монте-Кассино был чрезвычайно глубоким и сильным, потому 
        что и сама битва стала недюжинным явлением в истории войн. Поле битвы 
        Монте-Кассино уже было знаменито во всем мире неудачами прежних атак союзников 
        и бомбардировками монастыря. Этот факт должен был сказаться на психике 
        солдат, которым в ближайшее время предстояло включиться в эту битву.<br>
        <br>
        Во время всех предшествующих сражений под стенами монастыря встречались 
        только «противники». Теперь же должны были столкнуться «враги». Взаимная 
        ненависть, передававшаяся почти тысячу лет из поколения в поколения, чувство 
        обиды, глубоко вросшее в польскую душу, невиданные преступления, совершенные 
        оккупантами в стране, — всё это предопределяло, что борьба будет непримиримой, 
        до последней капли крови.<br>
        <br>
        Противостояли друг другу два разных мировоззрения и две разных воли.<br>
        <br>
        В этих условиях кризис боя быстро нарастал по обе стороны фронта и становился 
        страшным. Прежде всего из-за людских потерь. Массированный артиллерийский 
        огонь обрушился на немцев в момент подготовки к передислокации частей 
        первой линии и принес большие потери как на монастырском участке, так 
        и в долине реки Лири. Немцы в это время уже не располагали такими многочисленными 
        материальными и людскими резервами, как ранее, поэтому латание каждой 
        дыры могло проводиться только перемещением сил с других участков фронта.<br>
        <br>
        Наши части в первую ночь боя были обескровлены так сильно, что кризис, 
        очень острый и глубокий, был неминуем. Совершенно естественно, что истинные 
        данные о потерях не отвечали картине, воссоздаваемой на основе личных 
        впечатлений и донесений, полученных в первые дни сражения. Каждая, даже 
        самая лучшая воинская часть состоит из людей разной физической и психической 
        стойкости. Следует принять за очевидное, что во время столь тяжкого боя 
        часть солдат воспользовалась возможностью переждать в каком-то безопасном 
        убежище в скалах либо отойти в тыл под предлогом эвакуации раненых или 
        просто по ошибке, заблудившись в такой запутанной, дикой и пересеченной 
        местности, плотно обстреливаемой и задымленной. Через некоторое время 
        люди находились, но в момент подсчета в ходе боя или сразу после него 
        их отсутствие засчитывалось как потери. Создавалось впечатление, что то 
        или иное подразделение вообще перестало существовать.<br>
        <br>
        Название «рота» или «батальон» приобретает для нас истинный смысл только 
        тогда, когда известно, сколько на самом деле людей и вооружения это подразделение 
        имеет. В период битвы за Монте-Кассино боевой состав наших пехотных рот 
        не превышал 60-80 человек. Поэтому любые потери, даже менее чувствительные, 
        чем действительно имевшие место, командиры переживали очень остро.<br>
        <br>
        Кризис в относительно малой степени коснулся рядовых и не мог проявиться 
        на низших уровнях командования, так как значительная часть унтер-офицеров 
        и младших офицеров, командовавших взводами, полуротами и ротами, была 
        убита или ранена. Те, кто уцелел, еще находились в пылу борьбы и ожидали 
        ее продолжения. Кризис пролегал лишь между двумя принципиальными возможностями 
        — жизни и смерти.<br>
        <br>
        А далее?<br>
        <br>
        А далее в игру вступало чувство ответственности за жизнь порученных тебе 
        людей, за выполнение поставленных задач, за способ ведения боя. Каждый 
        из командиров неоднократно подвергал сомнению свои действия, вновь и вновь 
        обдумывая, не совершил ли он ошибки, не пренебрег ли чем-то, были ли его 
        приказы достаточно точными. Батальон, бригада и дивизия были уровнем, 
        на котором кризис проявился сильнее всего.<br>
        <br>
        Разочарованные голоса, обескураживающие донесения о том, что некому дальше 
        вести бой, иногда даже критика приказов, поступающих от командиров, которые 
        были известны как люди смелые и стойкие, волна сомнений — всё это захлестнуло 
        корпус. Но здесь, однако, не вызвало того же резонанса. Никто даже из 
        близкого окружения не знал, что думает и что в глубине души переживает 
        их командир — генерал Андерс. Ни в лице его, ни в голосе ничто не выказывало 
        сомнения. В эти тяжелые дни он давал подчиненным впечатляющий пример спокойствия 
        и присутствия духа. Сердечно, по-дружески, но твердо он поддерживал в 
        них энергию и веру в победу.<br>
        <br>
        Как на самом деле выглядел кризис и как он был преодолен, я мог видеть 
        по горячим следам назавтра после захлебнувшейся атаки. Из штаба самым 
        тяжким казалось положение на участке 5 й Восточной дивизии.<br>
        <br>
        — Поезжай к ним, посмотри, как там дела, поговори с ними, — дал мне приказ 
        генерал Андерс. Вместе с генералом Суликом на двух «Виллисах» мы двинулись 
        в направлении фронта. Переправа через реку Рапидо, куда мы направлялись, 
        была как раз под огнем тяжелой артиллерии врага. Рядом находился пункт 
        водоснабжения передовых частей. Когда мы приблизились к реке, артиллерийский 
        огонь стих. В клубах пыли и дыма в окопах и траншеях стали видны люди, 
        которые спокойно взялись за только что прерванную работу — стали наполнять 
        водой жестяные фляги. Они улыбались нам и подшучивали друг над другом, 
        обсуждая недавний налет и недопитый цикорий.<br>
        <br>
        Дальше мы поднимались по скалистой расселине, в которой отдыхал 15-й батальон, 
        один из тех, что понесли наибольшие потери. Уже у подножья из блиндажа 
        вышел удивительно молодой подпоручик с лицом, черным от дыма и грязи, 
        с красными от недосыпания глазами, отдал честь и четким уверенным голосом 
        заявил:<br>
        <br>
        — Господин генерал, прошу немедленно надеть каску. Комбат запретил находиться 
        без каски в расположении батальона! — Мы оба надели каски и начали подъем. 
        Хотя и без вещмешков, мы обливались потом, солнце сильно пекло. Несколько 
        раз мы останавливались, чтобы поговорить с солдатами и отдохнуть. Слышали 
        вопросы о ходе битвы и слова, полные веры, что победа будет за нами. Солдатские 
        руки протягивали нам фляжки с водой, а ведь люди даже не брились, чтобы 
        не растратить ни капли бесценной влаги.<br>
        <br>
        На середине расселины нас нагнали командир батальона подполковник Каминский 
        и его заместитель майор Жихонь, возвращавшиеся с совещания в штабе бригады. 
        Оба улыбались, были спокойны. Только усталые и покрасневшие глаза смотрели 
        серьезно и сурово. У самой линии огня, чуть в стороне от солдатских окопов, 
        был блиндаж командира батальона. Здесь нас никто не мог видеть. Лицо подполковника 
        Каминского неожиданно изменилось, погрустнело, осунулось. На мой вопрос 
        о положении батальона он тихо ответил, что батальона больше нет. Те несколько 
        десятков человек, которых мы видели по дороге, — это все, кто уцелел.<br>
        <br>
        В голосе этого человека звучало отчаяние. Понимание, что атака не удалась, 
        что ее нужно повторить, а в батальоне не наберется людей даже на роту, 
        угнетало его и лишало обычного пыла и решимости. Он еще не знал, что новое 
        наступление начнется через несколько дней, а за это время батальон пополнится, 
        потому что вернутся рассеянные и заблудившиеся люди и легкораненые из 
        госпиталя.<br>
        <br>
        Он быстро обрел равновесие и энергично взялся приводить в порядок остатки 
        батальона и готовиться к новому бою. Но уже знал, насколько высокую цену 
        нужно заплатить за победу. Знал, что сам поведет своих солдат. Может быть, 
        предчувствовал, что отдаст здесь свою жизнь «за Польшу», как скажет умирая. 
        С теми же словами погиб и его заместитель, майор Жихонь.<br>
        <br>
        Началась вторая атака. По мере ее развития стал нарастать новый кризис 
        — на этот раз от физической и психической усталости солдат.<br>
        <br>
        Пришли минуты последних схваток, так прекрасно описанных Ваньковичем, 
        когда надо было сделать всего лишь еще одно усилие, чтобы склонить весы 
        победы в нашу сторону. Но на это у солдат уже не было сил.<br>
        <br>
        Это были те минуты боя, когда у командира остается только один резерв 
        — он сам. Именно в таком положении оказался командир 5-й бригады полковник 
        Курек. Он, не колеблясь ни минуты, пошел вперед, чтобы преодолеть этот 
        кризис, пошел, потому что солдаты должны знать, что бой продолжается и 
        командир впереди.<br>
        <br>
        Пошел и погиб.<br>
        <br>
        За победу. За Польшу.<br>
