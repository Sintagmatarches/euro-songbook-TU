        <br>
        <br>
        <strong>Петр Мицнер</strong><br>
        <em><strong><br>
        В мае нынешнего года исполнится 65 лет со дня окончания II Мировой войны. 
        В связи с этим мы вспоминаем два сражения — под Ленино (12-13 октября 
        1943) и при Монте-Кассино (12-18 мая 1944). Оба чрезвычайно тяжелых и 
        кровавых. И оба сражения приобрели символическое значение, что, с одной 
        стороны, возвысило их тяготы, страдание и смерть, а с другой — отняло 
        у них человеческое измерение.</strong></em><br>
        <br>
        <br>
        <span class="header"><strong>ЛЕНИНО<br>
        </strong></span> <br>
        В сражении под Ленино 12 и 13 октября 1943 приняла участие 1-я пехотная 
        дивизия имени Тадеуша Костюшко. Почти полвека правда об этой битве была 
        практически недоступна, искажена цензурой, скрыта в архивах. Только недавно 
        историки начали подступать к ней на основе источников. Попробуем суммировать 
        их выводы.<br>
        <br>
        Сформированная в Сельцах на Оке 1-я дивизия, состоявшая из недавних зэков 
        и ссыльных, была брошена в бой без достаточной подготовки. Это было сделано 
        по просьбе командира — генерала Зигмунта Берлинга<sup>1</sup> и основательницы 
        Союза польских патриотов Ванды Василевской. После долгого, тяжелого марша 
        дивизия достигла места, где должна были прорвать линию немецкой обороны. 
        Берлингу, однако, не сообщили о планах советского командования, не знал 
        он также, каковы силы неприятеля.<br>
        <br>
        12 октября на рассвете в разведку боем был послан 1-й батальон под командованием 
        майора Бронислава Ляховича<sup>2</sup>. Потери были огромны. Решив, что 
        немцы отходят, Берлинг дал приказ начать общее наступление. Артиллерийская 
        подготовка из-за нехватки боеприпасов не удалась. На польских и советских 
        солдат обрушился удар бомбардировщиков; зенитной артиллерии не было.<br>
        <br>
        После двухдневного боя польская дивизия была отведена. Погибло 510 офицеров 
        и солдат, раненых было около 1800, многие попали в плен.<br>
        <br>
        Было ли нужным сражение под Ленино? Об этом еще долго будут спорить исследователи. 
        Военный историк Станислав Ячинский пишет, что оно имело тактическое значение, 
        но «участок фронта, который занимала польская дивизия, оставался [неподвижным] 
        вплоть до июня 1944»<sup>3</sup>. Можно встретить и более радикальные 
        оценки.<br>
        <br>
        Мы сегодня знаем больше, чем десять-двадцать лет назад, но еще не всё.<br>
        <br>
        Бесценный источник для истории сражения и в целом для истории армии Берлинга 
        — хроникальные записи, которые вел Люциан Шенвальд, до сих пор полностью 
        не опубликованные. Шенвальд (1909-1944) был поэтом, одним из виднейших 
        в своем поколении. До войны следовал поэтике катастрофизма, ему был близок 
        сюрреализм. При всем том еще с молодости Шенвальд был коммунистом. Во 
        время войны находился сначала во Львове, где много печатался, работал 
        на радио. После 22 июня 1941 г. пошел в Красную армию, но вскоре был арестован 
        и сослан в Сибирь. В 1943-м вступил в 1-ю дивизию и стал ее летописцем. 
        Погиб в автомобильном крушении в 1944 году. Хроника долго считалась утраченной. 
        Вот ее фрагменты, фиксирующие совещания после битвы под Ленино.<br>
        <br>
        <br>
        <strong>Люциан Шенвальд</strong><br>
        <strong><br>
        <span class="header">ХРОНИКА 1-Й ПЕХОТНОЙ ДИВИЗИИ<br>
        ИМЕНИ ТАДЕУША КОСТЮШКО<br>
        (отрывки)</span></strong><br>
        <br>
        <br>
        <strong>Совещание после сражения. 15.10.43</strong><br>
        <br>
        (На стенах избы в Николаевке, где расположен 1-й отдел штаба, висит плакат 
        с надписью «Ленино. 12.10.43 — 13.10.43».)<br>
        <br>
        (Телефон выключен.)<br>
        <br>
        Время 10.00.<br>
        <strong><br>
        Генерал [Зигмунт Берлинг]:</strong><br>
        <br>
        Почтим минутой молчания память погибших.<br>
        <br>
        (Все встают.)<br>
        <br>
        Ну, садимся.<br>
        <br>
        Боевые действия дивизии в течение 12 и 13 октября дали ценный опыт, за 
        который мы дорого заплатили. Было много причин, не зависящих от нас, которые 
        привели к такому положению вещей, но в нас самих, в дивизии, был источник 
        ошибок, без которых потери были бы меньше.<br>
        <br>
        1. Задачу прорвать фронт дивизия выполнила. Фронт прорван, дивизия выдвинулась 
        вперед, подойдя с одной стороны под Тригубово, с другой — под Ползухи.<br>
        <br>
        Выдвинувшись вперед, дивизия из-за бездействия соседей оказалась в мешке, 
        но [неприятеля] обратно не пустила.<br>
        <br>
        2. 1-й пехотный полк, согласно приказу, пошел в наступление и прекрасно 
        продвигался до Тригубова. Деревня Тригубово была вне нашей линии, но из-за 
        бездействия соседей 1-й полк оказался под фланговым огнем и дальше продвигаться 
        не мог. Тогда я приказал брать Тригубово. Деревня была взята 1-м полком, 
        который вышел под фланговым огнем [неприятеля], занявшего высоты к югу 
        от Тригубова. [Неприятель] пошел в контратаку и выбил нас из Тригубова. 
        Со стороны Пунища пошли бронетранспортеры и танки [неприятеля]. С этого 
        момента в полку начался основной непорядок. В чем причина?<br>
        <br>
        Полк получил приказ наступать тремя эшелонами. Неизвестно почему, но, 
        дойдя до Тригубова, полк имел три батальона в одном эшелоне. Эшелоны смешались: 
        второй с первым, а потом третий со вторым.<br>
        <br>
        Командование было дезорганизовано. Командир 1-го полка потерял управление 
        полком, и то, что там дальше происходило, носило все черты паники.<br>
        <br>
        После вечернего донесения Кеневича<sup>4</sup>, что дела плохи, я решил 
        ввести в действие второй эшелон дивизии.<br>
        <br>
        Командир 1-го полка<sup>5</sup> оставил полк, сам оказался в Ползухах 
        и сообщил, что полк разбит, что его нет. Я приказал ему вернуться и взять 
        командование над остатками полка.<br>
        <br>
        Как видно, командир полка утратил власть над полком с самого начала и 
        уже не владел батальонами, а когда [неприятель] пошел в контратаку, у 
        него уже не было средств противостоять тому, что случилось, — катастрофе 
        1 го полка. (...)<br>
        <br>
        Если руководство боем не стояло на уровне задачи, то командование ответственно 
        за потери. Но это не освобождает от ответственности командиров, у которых 
        было в распоряжении достаточно тяжелого вооружения, чтобы действовать 
        самим. (...)<br>
        <br>
        Танки попали в очень плохие условия. Саперы строили мост, рассчитанный 
        на 60 тонн, но на лугах, где вязли даже телеги, не было настила, поэтому 
        завязли и танки. Ночью я получил приказ ввести танки в бой, но танки не 
        могли выполнить этот приказ.<br>
        <br>
        [Другим] танкам не нравилось сражаться в боевых порядках пехоты — они 
        принялись маневрировать. Эту роту вам уже до конца войны не найти. (...)<br>
        <br>
        Один из командиров сказал, что вы получили приказ [генерала Берлинга] 
        перевязать раненых и оставить их на месте. Раненые не были перевязаны 
        и лежали по два-три дня. Вы плохо поняли мой приказ. Был приказ, что людям 
        из боевых порядков отлучаться для доставки раненых в тыл нельзя. Это не 
        значит, что медицинская служба должна ждать, пока я подберу раненых. Мне 
        пришлось посадить начальника штаба в машину и отправить по полкам. Позавчера 
        я приказал, чтобы полки, отходя, оставили по два взвода: один — подобрать 
        раненых и убитых, второй — для сбора оружия, своего и трофейного.<br>
        <br>
        Наших раненых подбирали части Красной армии. Правда, и у нас есть раненые 
        красноармейцы. Но это только малая часть. Полковник Галицкий<sup>6</sup> 
        подтверждает факт, что наши раненые по три дня лежали без еды. (...)<br>
        <br>
        1. Поэтому. Выяснить всё, чем располагаем, определить потери — людские, 
        материальные. Это сделать еще сегодня.<br>
        <br>
        2. Провести проверку личного состава и перекомплекта.<br>
        <br>
        3. Предложить персональные изменения.<br>
        <br>
        4. Восполнить нехватки. Собрать с поля боя всё, что там осталось.<br>
        <br>
        <strong>Совещание 16.10.1943. Время 18.00<br>
        <br>
        Полковник Кеневич:</strong><br>
        <br>
        Плохо были прикрыты фланги.<br>
        <br>
        Когда стало ясно, что соседи<sup>7</sup> не двинулись, надо было сразу, 
        не дожидаясь приказа, выделить силы на защиту флангов.<br>
        <br>
        Наибольшие потери были не при прорыве обороны [неприятеля] (самое большее 
        30 убитых), а как раз позже, дальше, из-за оголения флангов.<br>
        <br>
        Трофеи надо собрать. Убитых похоронить.<br>
        <br>
        <strong>Генерал:</strong><br>
        <br>
        Если бы мы хотели сделать выводы из того, что случилось, то должны были 
        бы разве что взять револьвер и расстреливать.<br>
        <br>
        Прекрасных поступков, прекрасных примеров было много. Мы видели людей, 
        которых надо выдвигать. У нас уже есть свои герои.<br>
        <br>
        Но есть и сукины сыны, которых надо расстреливать.<br>
        <br>
        <strong>17 октября 1943, Николаевка.<br>
        <br>
        Стенограмма совещания командования 1-й пехотной дивизии с офицерами по 
        политико-воспитательной работе<br>
        <br>
        Полковник Сокорский<sup>8</sup>:</strong><br>
        <br>
        Мы собрались, чтобы сделать определенный анализ событий 12 и 13 октября 
        и обсудить нашу работу на будущее.<br>
        <br>
        1. О боевых недостатках говорил командир. Мы здесь затронем несколько 
        другую сторону дела.<br>
        <br>
        Мы готовили дивизию не к такому ходу событий, как тот, который имел место 
        в действительности. Мы должны были принять участие в общем советском наступлении 
        после соответствующей артподготовки и под прикрытием с воздуха. Однако 
        война есть война, и в ней есть свои случайности.<br>
        <br>
        Мы не были частью общего наступления. Для прорыва [обороны] был выбран 
        участок едва шестикилометровый. И неприятель не только не собирался отходить, 
        как нам сообщили, но сделал всё, чтобы удержать нас от выхода к Днепру, 
        что означало бы для него разрыв главных линий [коммуникации]. Пользуясь 
        тем, что общего наступления не было, неприятель собрал мощный артиллерийский, 
        танковый и авиационный кулак. Надо было немедленно пересмотреть весь план 
        операции, изменить систему боя в ходе сражения. Это очень трудно и требует 
        опытных командиров и солдат — и мы этого сделать не смогли, хотя наша 
        дивизия и оказалась тут лучшей.<br>
        <br>
        Если бы наши соседи сделали то же, что мы, мы не оказались бы в мешке, 
        и наша задача была бы выполнена.<br>
        <br>
        В условиях, в которых мы были, мы сделали многое как дивизия, как солдаты. 
        Мы прорвали оборону неприятеля, не дали себя окружить. Но мы уткнулись 
        в старые планы и не достаточно быстро изменили систему боя. Мы пытались 
        продолжать наступление, хотя объективных возможностей к этому не было.<br>
        <br>
        Это повлекло за собой серьезные последствия, особенно что касается 1-го 
        пехотного полка, который сначала действовал отлично. Но командование 1-го 
        полка не смогло овладеть ситуацией в условиях контратак с флангов.<br>
        <br>
        Наша самая большая ошибка состоит в том, что мы недооценили силы противника 
        и не перестроили систему боя, но мы и не могли ее перестроить, потому 
        что командиры не руководили своими частями как следует.<br>
        <br>
        Боевые и политические офицеры вели себя как герои — но герои на уровне 
        командира роты и даже взвода. Надо было меньше подвергать себя опасности, 
        а больше руководить. (...)<br>
        <br>
        Наш солдат проявил невероятный героизм. Командиры Красной армии говорят, 
        что они не видели частей, которые бы наступали под таким огнем. Но — говорят 
        [нрзб]. Сами пострадали от своего героизма. Под огнем тяжелой артиллерии 
        нельзя ложиться, а под пулеметным — надо.<br>
        <br>
        Наш солдат при большом героизме проявил недостаточную психическую устойчивость. 
        Всегда был только шаг от героизма до паники. Наш солдат не закален в бою. 
        Никогда еще дивизию сразу не вводили в такой бой. Были, однако, и случаи 
        большого упорства в бою.<br>
        <br>
        Например, сорок автоматчиков, которые окопались в тылу врага и держались 
        [полтора] суток. После этого один из них сообщил, что кончаются боеприпасы. 
        Тогда всем приказано было отходить. Командиры о них не знали. Офицеры 
        проявили большую отвагу, но все они себя вели как командиры роты. Иногда 
        этот героизм был просто вредным. Командиры полков и батальонов под огнем 
        ходили себе поверху траншеи.<br>
        <br>
        Первое сражение — это первое сражение, и проявить героизм в нем необходимо. 
        Но это должен быть разумный героизм. (...)<br>
        <br>
        В целом поведение офицеров было замечательным.<br>
        <br>
        Как коммунисты (Калиновский<sup>9</sup>), так и пилсудчики (Заблудовский<sup>10</sup>), 
        сторонники Сикорского или крестьянской партии — все они показали, что 
        Польша может создать единый большой национальный лагерь.<br>
        <br>
        В бою было много героизма, который пошел прахом, потому что командир не 
        выполнил свою работу.<br>
        <br>
        Если офицер-политработник будет об этом помнить и заставлять командира 
        работать, то мы с такой историей больше не столкнемся.<br>
        <br>
        Пехотные полки имели по два, а потом по одному артиллерийскому дивизиону, 
        а задачи должен был решать командир артиллерии дивизии. Снарядов было 
        много, но били по своим, потому что артиллерии не сказали, куда бить. 
        А немецких пулеметов не подавили.<br>
        <br>
        Этот опыт нам дорого обошелся, другого такого опыта мы не переживем, потому 
        что у нас погибнут лучшие.<br>
        <br>
        Ну, это всё.<br>
