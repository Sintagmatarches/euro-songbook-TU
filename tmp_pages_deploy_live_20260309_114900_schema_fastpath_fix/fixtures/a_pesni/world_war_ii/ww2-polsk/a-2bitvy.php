        ли, но знай, сколько от тебя зависит». Слова светлой памяти майора Строевского 
        автор этой книги запомнил как завет на всю оставшуюся жизнь и на каждое 
        дело, которым он занимается.<br>
        <br>
        «Помни, сколько от тебя зависит». Грудзинский помнит, что, участвуя в 
        борьбе нынешнего поколения за то, чтобы не одичать на отдаленном острове, 
        он должен, как на склонах Монте-Кассино, на каждой своей странице держать 
        неразрывную связь с живыми и мертвыми, защищая «незримый град, который 
        характеры людские стерегут».<br>
        <br>
        <br>
        <strong>Норман Дэвис<br>
        <br>
        ИЗ ПРЕДИСЛОВИЯ К КНИГЕ МЕЛЬХИОРА ВАНЬКОВИЧА (2009)</strong><br>
        <br>
        Битва за Монте-Кассино велась не только между немцами и поляками. Это 
        была сложная, долгосрочная операция, в которой многочисленные формирования 
        Вермахта и Люфтваффе столкнулись с двумя союзническими армиями — американской 
        5-й армией под командованием генерала Марка Кларка и 8 й британской генерала 
        сэра Оливера Лиса. Второй корпус генерала Андерса, сражавшейся в рядах 
        8-й армии под британским флагом, был лишь одним из многих воинских соединений, 
        что, впрочем, ничуть не умаляет его заслуг.<br>
        <br>
        Следует также отметить, что итальянская кампания 1943-1945 гг., в ходе 
        которой произошла битва под Монте-Кассино и была прорвана «линия Густава», 
        не проходила в соответствии с ожиданиями союзников. После падения фашистского 
        режима в Италии главной стратегической целью командующего немецким фронтом 
        генерала Кессельринга было задержать наступление союзнических сил и не 
        дать им перейти альпийские границы Третьего Рейха. Стратегия Кессельринга 
        оказалась верной — планы союзников были сорваны. Даже в самом конце войны, 
        в мае 1945 г., 5-я и 8-я армия всё еще находились далеко от Альп. Поляки 
        дошли до Болоньи и Римини, после чего были остановлены. Вопреки наполеоновским 
        планам Черчилля, ни один солдат союзнических армий даже не приблизился 
        к Люблянскому перевалу. Разумеется, нельзя говорить, что жертва, принесенная 
        при Монте-Кассино, была напрасной, как можно вычитать в некоторых прогорклых 
        комментариях, нельзя и считать, что итальянская кампания не имела влияния 
        на окончательную победу союзнических сил. Напротив: взятие Монте-Кассино 
        польскими войсками спасло западные силы от конфуза в мае 1944-го, когда 
        их успехи на других фронтах оставляли желать много лучшего, в то время 
        как сталинская Красная армия крушила немцев на восточном фронте. Сталинградская 
        битва была, несомненно, более масштабной, однако Монте-Кассино принесло 
        сходный результат, показав, что фашистская военная машина не непобедима, 
        и это позволяло не утратить надежду на успех. Освобождение Рима, Вечного 
        города, путь к которому лежал через Монте-Кассино, имеет ранг события 
        большого политического, психологического и символического значения. Более 
        того, после выхода Италии из Оси и нейтрализации многочисленной итальянской 
        армии бои на полуострове значительно ослабили немцев и сыграли немалую 
        роль в разгроме гитлеровского Рейха. Конечно, это не было безусловным 
        триумфом, который американцы и британцы склонны считать своим, однако 
        нельзя говорить и о позорной неудаче.<br>
