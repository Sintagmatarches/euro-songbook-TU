  <br>
  Песня сложена в январе-феврале 1943 г. бойцами сводного отряда 
  альпинистов 897-го горнострелкового полка 242-й стрелковой дивизии Закавказского 
  фронта, воевавшими в районе Эльбруса. Первые строки сочинили Любовь Коротаева и Николай Персиянов в начале января 1943 г. Мелодия - из  танго <a href="../../cabaret/pustdni.php">&quot;Пусть 
  дни проходят&quot;</a> (слова Ильи Финка, музыка Бориса Терентьева, 1938; на эту же мелодию создана <a href="golubi.php" target="_blank">"Жил в Ростове Витя Черевичный"</a>). В дальнейшем &quot;Баксанская&quot; (а также <a href="barbaris.php">&quot;Барбарисовый 
  куст&quot;</a>) стала популярной песней альпинистов и туристов. Есть ее дальнейшие 
  переделки времен войны в Афганистане 1979-1989 - см. <a href="../../afgan/bojgremel.php">&quot;Бой 
  гремел в окрестностях Кабула...&quot;</a><br>
  <br><br>
  <strong>Из книги инструктора военного альпинизма Михаила Боброва, который воевал на Кавказе вместе с авторами песни (Бобров М.М. Фронт над облаками. СПб.: СПбГУП, 2005. С. 114-116.):</strong><br>
  <br>
  <em>В январе 1943 года наши разведчики получили приказ добыть сведения о расположении на склонах Чегета огневых точек и позиций фашистских горных егерей. Для этого следовало забраться на высоту свыше 4000 метров. Группа в составе одиннадцати воинов под командованием лейтенанта А.И. Николаева, двух инструкторов военного альпинизма - Любы Коротаевой и Андрея Грязнова - поднималась с юга по склонам Донгуз-Оруна. Было морозно. Ночь застала бойцов на скалах. Ночевали, подстелив под себя полушубки и накрывшись сверху другими. Утром Люба с Андреем полезли выше, а группа Николаева осталась для прикрытия. Они вышли на гребень между Малым Когутаем и Донгуз-Оруном и залегли, чтобы не выдать свое присутствие. Ниже, метрах в пятидесяти, находились немцы. Наши ребята пролежали весь день, наблюдая и уточняя расположение противника не только на склонах Чегета, но и у &quot;Старого кругозора&quot;, в Баксанском ущелье. Внизу под ними четко были видны огневые позиции и темные фигуры часовых.<br>
  <br>
  Подстраховывая друг друга. Люба и Андрей скрытно продвигались по гребню. Во время этой разведки по старой альпинистской традиции здесь же на гребне сложили из камней тур-пирамидку и оставили там гранату с запиской вмесо взрывателя. Пройдут годы, думали они, и найдут эту записку альпинисты мирного времени.<br>  
  <br>
  (Так и случилось. Московский студент, альпинист Симоник, обнаружил в горах тур военных лет, в нем гранату, в которой лежала записка:<br>
  <br>
  &quot;В дни, когда враг побежал под ударами Красной армии, мы поднялись сюда без веревок и палаток, в шубах и валенках, по суровым стенам Донгуз-Оруна, чтобы указать путь наступающим войскам.<br> <br> 
  Коротаева, Грязнов - инструкторы альпинизма.<br> 
  03.01.1943 г.&quot;<br> 
  <br> 
  Под впечатлением успешно выполненной разведки и благополучного возвращения на базу Люба и Андрей сложили песню, первый куплет которой начинался словами:<br> <br>  
  Помнишь, товарищ, белые снега,<br>  
  Стройный лес Баксана, блиндажи врага?<br>  
  Помнишь гранату и записку в ней<br>  
  На скалистом гребне для грядущий дней?..<br>  
  <br>  
  В последующие дни после ряда боевых действий и снятия фашистских флагов с Эльбруса начало песни было дополнено другими куплетами, которые помогали составить их друзья альпинисты - Николай Моренец и Николай Персиянов. Узнав об этом и впервые услышав песню, Юрий Визбор долго искал лейтенанта Любу Коротаеву. Он сделал интересную передачу на радио, рассказал о создании этой песни и сам исполнил ее под аккомпанемент гитары.<br>  
  <br>  
  так коллективно родилась песня &quot;Баксанская&quot;, которую в туристских и альпинистских лагерях поют по сегодняшний день:<br> <br>
