  Андрей Грязнов погиб в 1949, исполняя обязанности начальника спасательного отряда, во время сложных спасательных работ. Пик Андрея Грязнова (4421 м) находится в Киргизском хребте на Тянь-Шане. <br> 
  <br>
  Любовь Коротаева стала доцентом Университета дружбы народов имени Патриса Лумумбы, где и проработала всю жизнь. Кандидат химических наук. Скончалась 18 января 2000 года в Москве. <br>
