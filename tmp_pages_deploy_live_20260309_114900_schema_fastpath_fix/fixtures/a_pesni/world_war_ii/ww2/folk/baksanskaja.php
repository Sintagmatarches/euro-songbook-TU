  <strong>Из жж-сообщества <a href="http://wartime-muzik.livejournal.com/7785.html" target="_blank">&quot;Война, музыка и песня&quot;</a>, опубликовал 2 апреля 2012 года <a href="http://glavholod.livejournal.com/" target="_blank">glavholod</a> на основе разных источников:</strong><br>
  <br>
  <em>В 1938 году выпускник Московской консерватории, композитор Борис Терентьев, вдохновившись  стихотворением «Семья» поэта Ильи Финка, создает мелодию танго «Пусть дни проходят».<br>
  <br>
  В начале 1943 года появилась песня «Баксанская», написанная на мелодию танго Терентьева, альпинистами - бойцами сводного отряда альпинистов 897-го горнострелкового полка 242-й стрелковой дивизии Закавказского фронта: А. Грязновым, Б. Грачевым, Л. Коротаевой, Г. Сулкавидзе, А. Немчиновым и Н. Персияниновым. Признанными же авторами в большинстве песенников считаются Л. Коротаева, А. Грязнов и Н. Персиянинов. <br>
  <br>
  За несколько дней до проведения боевой операции лейтенанты Коротаева и Грязнов вышли в разведку на гребне хребта между Малым Кугутаем и Донгуз-Оруном. Они оставили под каменным туром разряженную гранату с запиской: "В дни, когда враг побежал под ударами Красной Армии, мы поднялись сюда без веревок и палаток, в шубах и валенках по суровым склонам Донгуз-Оруна (а немцы использовали здесь специально подготовленных, хорошо экипированных и оснащенных для действий в горах егерей дивизии "Эдельвейс". - ) чтобы указать путь наступающим бойцам..." Когда вернулись в отряд, неожиданно сложилась песня, она начиналась со слов "Помнишь гранату и записку в ней...". <br>
  <br>
  Есть выпуск звукового журнала "Кругозор" 1968 года с репортажем Юрия Визбора, где Любовь Коротаева (и, очевидно, Александр Гусев) вспоминает о создании песни:<br>
  <br>
&quot;Однажды мы с Андреем Грязновым получили задание: установить пути отступления немцев с Кавказа. Целый день пролежали в снегу на хребте Когутай. А в такой обстановке разные мысли приходят, и мы решили это место отметить. Вынули из одной гранаты запал, написали, что такого-то числа здесь были в разведке лейтенанты
Грязнов и Коротаева. Потом сложили тур из камней и положили туда гранату.<br>
<br>
Договорились, что кто первый после войны придет - сообщит другому, что снял эту гранату. Ни ему, ни мне не пришлось больше там побывать...
Пришло время. Нам приказали сорвать немецкие флаги. Начался путь из Приэльбрусья.<br>
<br>
Когда мы остановились в Итколе ночевать, то установили дежурство на веранде балкарского домика. Была чудная лунная ночь, и как раз из Иткола было очень хорошо видно гребень, на котором оставили мы гранату. Все вышли на балкон. Андрей Грязнов стал напевать про себя песню, а потом случайно у него сложились слова: "Помнишь гранату и записку в ней". А потом кто-то добавил: "На скалистом
гребне для грядущих дней". И потом у нас такой энтузиазм сразу вдруг появился к сочинению, получилось что-то такое, и начали создавать...&quot;<br>
  <br>
 Николай Персиянинов не дожил до Победы. Гвардии старший лейтенант Персиянинов, комвзвода 6 отд. гв. разведроты 2-ой Воздушно-десантной Проскуровской дивизии, погиб в бою 3 апреля 1945 года. <br>
  <br>
  <img src="persianov.jpg" width="776" height="1095"><br>
