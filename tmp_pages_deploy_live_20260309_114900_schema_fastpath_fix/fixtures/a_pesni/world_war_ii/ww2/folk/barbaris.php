«Барбарисовый куст» (а сложенная в той же группе кавказских военных альпинистов <a href="baksanskaja.php">«Баксанская 
фронтовая»</a>) была популярна у туристов 1960-х гг. См. о ней: Ю. Визбор. 
Верю в семиструнную гитару. М.: Аргус, 1996. Очерк &quot;Легенда седого Эльбруса&quot;.<br>
