<strong>ВАРИАНТЫ (3)<br>
<br>
1. Барбарисовый куст</strong><br><br>
Мне не забыть той долины,<br>
Холмик из серых камней.<br>
Где ледоруб вполовину<br>
Воткнут руками друзей.<br>
<br>
Ветер тихонько колышет,<br>
Гнет барбарисовый куст.<br>
Парень уснул и не слышит<br>
Песни сердечную грусть.<br>
<br>
Тропка, как ленточка, вьется,<br>
Горная речка шумит.<br>
Кто в ту долину вернется,<br>
Холмик кто тот посетит?<br>
<br>
И на вечернем досуге,<br>
В скалах мерцает огонь.<br>
Грустную песню о друге<br>
Тихо играет гармонь.<br>
<br>
Ветер тихонько колышет,<br>
Гнет барбарисовый куст.<br> 
Парень уснул и не слышит
<br>
Песни сердечную грусть.<br>
<br>
<em>Бобров М.М. Фронт над облаками. СПб.: СПбГУП, 2005. С. 155-156. [Автор книги - участник событий]</em><br><br><br>
<strong>2. Барбарисовый куст</strong><br>
<br>
Мне не забыть той долины,<br>
Сложенный холм из камней,<br>
Где ледоруб в середину<br>
Воткнут руками друзей.<br>
<br>
<em>Припев:</em><br>
<br>
Ветер тихонько колышет,<br>
Гнет барбарисовый куст.<br>
Парень уснул и не слышит<br>
Песен сердечную грусть.<br>
<br>
<em>Припев.</em><br>
<br>
Слышатся грома раскаты,<br>
Пушечных выстрелов гром:<br>
Там, за горою высокой,<br>
Други дерутся с врагом.<br>
<br>
<em>Припев.</em><br>
<br>
Тропка, как ленточка, вьется,<br>
Горная речка шумит.<br>
Кто в ту долину вернется,<br>
Холмик он пусть посетит…<br>
<br>
<em>Припев.</em><br>
<br>
А на последнем привале <br>
В скалах мерцает огонь.<br>
Грустную песню о друге<br>
Тихо заводит гармонь.<br>
<br>
<em>Припев:</em><br>
<br>
Ветер тихонько колышет,<br>
Гнет барбарисовый куст,<br>
Песен героя не слышит<br>
Старый высокий Эльбрус.<br>
<br>
<em>Из архива пермского писателя Ивана Лепина<br>
<br>
В нашу гавань заходили корабли. Пермь: Книга, 1996. - без заглавия.</em><br>
