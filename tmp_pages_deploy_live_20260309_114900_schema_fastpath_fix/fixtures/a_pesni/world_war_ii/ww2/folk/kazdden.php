        <h1>КАЖДЫЙ ДЕНЬ СТОИТ НА ПОЛУСТАНКЕ</h1>
        
        Каждый день стоит на полустанке<br>
        Полторы минуты эшелон.<br>
        Разные бывают эшелоны,<br>
        За вагоном следует вагон.<br>
        Машинист в замасленной рубашке,<br>
        На перроне девушка одна<br>
        В ярко-красной форменной рубашке<br>
        В кителе из черного сукна. <br>
        <em>(2 раза)</em><br>
        <br>
        <em>Припев: </em><br>
        <br>
        Станция, деревья сонные,<br>
        Улыбка нежная и локон золотой, <br>
        Эх, синие, глаза бездонные<br>
        Запоминаются с минуты той. <br>
        <em>(2 раза)</em><br>
        <br>
        Но однажды как-то из вагона<br>
        Показался летчик молодой.<br>
        Золотые звезды на погонах,<br>
        На груди был орден боевой.<br>
        Девушка внезапно растерялась,<br>
        Заблестели радостью глаза.<br>
        И не знаю, как так оказалось —<br>
        Поезд был задержан полчаса. <br>
        <em>(2 раза)</em><br>
        <br>
        <em>Припев: </em><br>
        <br>
        И теперь, бывало, как и прежде,<br>
        Девушка выходит на перрон<br>
        И с улыбкой, затаив надежду,<br>
        Провожает каждый эшелон.<br>
        А когда конверт голубоватый<br>
        Главный достает не торопясь —<br>
        Это летчик, штурмовик крылатый,<br>
        С фронта шлет привет советский ас.<br>
