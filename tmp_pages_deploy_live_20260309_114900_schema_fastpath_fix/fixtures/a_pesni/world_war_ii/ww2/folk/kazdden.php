    Каждый день стоит у старых клёнов<br>
Полторы минуты эшелон.<br>
Всех родов бывают там вагоны,<br>
За вагоном следует вагон.<br>
Стрелочник в промасленной рубашке,<br>
На перроне девушка одна<br>
В ярко-красной форменной фуражке<br>
В кителе из черного сукна. <br>
<em>(2 раза)</em><br>
<br>
<em>Припев: </em><br>
<br>
