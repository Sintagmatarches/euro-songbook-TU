    <h1>ОХ, НЕТУ ГОЛОСУ ДА ПЕТЬ ЦЫГАНОЧКУ...</h1>

    Ох, нету голосу да петь цыганочку,<br>
    Сейчас спою я вам про партизаночку.<br>
    Жила девчоночка, в школе училася,<br>
    Да с той девчоночкой беда случилася.<br>
    Беда случилася да в утро раннее,<br>
    Напала на СэСэСэР страна Германия.<br>
    Восстал народ страной да всёй советскою,<br>
    Восстал на смертный бой с силой немецкою.<br>
    А та девчоночка была гражданская,<br>
    Звалася Зоечка Космодемьянская.<br>
    Ах, Зоя девочка да черноокая,<br>
    Да косы длинные, сама высокая.<br>
    Сама высокая, берёзка стройная,<br>
    Любови верною она достойная.<br>
    Цвела цветочиком да красовалася,<br>
    Родная мамонька да любовалася.<br>
    Да как стоял фашист под самой Москвой,<br>
    Сердце девушки облилось тоской.<br>
    Распростилася с родной мамочкой,<br>
    И на фронт ушла партизаночкой.<br>
    Ах, Зоя-Зоечка Космодемьянская,<br>
    Стала Зои жизнь да партизанская.<br>
    Гимнастёрочку надела новую,<br>
    Стала Зои жизнь топерь суровая.<br>
    Ах, Зои жизнь топерь перьменилася,<br>
    Из винтовочки стрелять училася.<br>
    Как пришел приказ немцев штаб взорвать,<br>
    Зоя вызвалась его исполнять.<br>
    Снег покрыл поля да и кусточики,<br>
    Снег покрыл луга да и лесочики.<br>
    По кустам она продиралася,<br>
    По лесам она пробиралася.<br>
    Вот настала ночь темным-темно кругом,<br>
    А Зоя к штабу вот подобралась ползком.<br>
    Гранату кинула, уйти собралася,<br>
    Да немцам в лапы их она попалася.<br>
    Схватили Зоечку да озверелые,<br>
    Связали Зоины да ручки белые.<br>
    Били изверги ей щечки алые –<br>
    Ничего она им не сказала.<br>
    Пытали изверги да комсомолочку,<br>
    Пытали лютые враги да Зоечку,<br>
    Да издевалися над ней до вечера,<br>
    А Зоя им в ответ да ни словечика.<br>
    Вот утро темное идет село будить,<br>
    А Зою-Зоечку враги ведут убить.<br>
    Да привели ее ко двум большим столбам,<br>
    Она махнула тут да всем своим друзьям.<br>
    Вот тонка досочка да тонко скрипнула,<br>
    А Зоя-Зоечка что силы крикнула:<br>
    – Душите гадину да вы немецкую,<br>
    Пускай живет в веках страна советская!<br>
    Ой, не встречать топерь ей зорьки ясные,<br>
    Ой, не видать топерь ей солнце красное.<br>
    Стоит берёзонька у ей могилушки,<br>
    Закрыты глазоньки, петь нету силушки.<br>
    Смотрите, мальчики, смотрите, девоньки,<br>
    Что с Зоей-Зоечкой фашисты сделали.<br>
    Когда придет черёд стоять за Родину,<br>
    Не забывайте вы про стойкость Зоину!<br>
