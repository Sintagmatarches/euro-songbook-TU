    А Зоя девушка да черноокая,<br>
    Постигла Зоечку судьба жестокая.<br>
    Когда стал фашист под самой Москвой,<br>
    Сердце девушки облилось тоской.<br>
    Распростилася с родной мамочкой,<br>
    И на фронт ушла партизаночкой.<br>
    По кустам ползла, остерегалася<br>
    И до врагов Зоя одна добралася.<br>
    Схватили Зоечку да озверелые,<br>
    Связали Зоины да ручки белые.<br>
    Повели ее да к двум большим столбам,<br>
    А Зоя крикнула да таковы слова:<br>
    «Вперед за Родину, вперед за Сталина!<br>
    Бейте, товарищи, фашистску гадину!»<br>
    А Зоя девочка была молоденька,<br>
    Убита жизнь ее в семнадцать годиков.<br>
    Я кончаю петь партизанскую<br>
    Про Зою-Зоечку Космодемьянскую.<br>
