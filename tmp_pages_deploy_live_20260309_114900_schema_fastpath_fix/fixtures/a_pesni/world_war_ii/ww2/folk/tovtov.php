  Он умер геройски,<br>
  Сражался с поляками,<br>
  Поляк его ранил тижало».<br>
  <br>
  <em>Неизвестный источник </em><br>
  <br>
  <br>
  Существует множество трагикомических песенок со сходным сюжетом (см. <a href="../../dvor/sodesskogo.php">«С 
  Одесского кичмана»</a>). Героя смертельно ранят какие-нибудь ужасные, иногда 
  полумифические враги (германцы, белополяки, белофинны, вохровцы), и он произносит 
  своему товарищу монолог со словами «Товарищ, товарищ, болят мои раны…». <br>
  <br>
  Выходит так, что в данном случае наш герой участвует в советско-польской войне 
  1919-1920 годов либо в агрессии на Польшу в сентябре 1939 года.<br>
  <br>
  <br>
  <strong>Советско-польская война 1919-1920 гг. </strong><br>
  <br>
  В ноябре 1918 года завершилась Первая мировая война. 3 ноября капитулировала (а вскоре 
  распалась) Австро-Венгрия, 11 ноября Германия. Обе вывели свои войска с оккупированных 
  территорий. Польша объявила независимость (11 ноября), то же самое сделала Галиция, 
  провозгласив Западно-Украинскую народную республику. Украина объявляла независимость 
  еще при немцах, а теперь там шла Гражданская война и действовало несколько правительств, 
  как и в Советской России. Все эти государственные образования имели разные взгляды 
  на границы, и проблема была решена военным путем. В итоге, когда всё закончилось, 
  на карте остались лишь Польша (ей досталась Галиция) и Советская Россия, подчинившая 
  себе основную часть Украины.<br>
  <br>
  Советско-польская война длилась 20 месяцев. Первое боевое столкновение РККА 
  с польскими войсками произошло в феврале 1919 года на литовско-белорусской территории. 
  Летом 1919 года поляками была разбита и вытеснена с Западной Украины Галицийская армия, а Западно-Украинская 
  республика ликвидирована. В феврале 1920 года Галицийская Армия перешла на сторону 
  Красной Армии, влившись в нее. <br>
  <br>
  Самые яркие события развернулись в 1920 году. Заключив союз с украинским правительством 
  Петлюры, польская армия весной 1920 года вошла в Житомир и Киев. В это же время, 
  в мае 1920 года, большевики замкнули Врангеля в Крыму и двинули Красную Армию 
  (Западный фронт <a href="../../grvojna/Foto/tuhatchevskij.php">Тухачевского</a> 
  и Юго-Западный фронт Егорова) на Запад для ускорения мировой революции. Красные 
  стремительно дошли до Варшавы.<br>
  <br>
  Однако мировой революции не получилось. Поляки окопались вдоль Вислы. 15 августа 
  1920 года, в праздник Девы Марии, польская конница  вошла в тыл Красной 
  Армии и в крупнейшем в истории кавалерийском сражении разнесла идею экспорта 
  революции в пух и прах. Европа назвала это «Чудом на Висле». На 19 лет Польша 
  обрела независимость. Позже Тухачевский «отыгрался» на подавлении Кронштадта 
  и Антоновщины. <br>
  <br>
  18 марта 1921 года был подписан советско-польский Рижский договор, определивший границы. 
  Спорные украинско-белорусские территории (в польской традиции - &quot;кр<strong>е</strong>сы&quot;, 
  в российской - &quot;Западная Украина и Западная Белоруссия&quot;) - признаны 
  за Польшей, - но идеи самостийности в них не угасли и вылились в итоге в трагическую 
  историю бандеровского движения.<br>
  <br>
  <br>
  <strong>Польская кампания 1939 года</strong><br>
  <br>
  23 августа 1939 года СССР и Германия заключили Договор о ненападении, поделив 
  между собой Восточную Европу (так называемый пакт Молотова-Рибентроппа). 
  Советскому Союзу &quot;достались&quot; Прибалтика, Молдавия и Правобережная 
  Польша. В соответствии с этим пактом 
  СССР и Германия совместно атаковали Польшу (1 сентября - Германия, 17 сентября 
  - СССР). 22 сентября обе армии агрессоров устроили праздничный парад победителей 
  в городе Бжешч (бывш. Брест-Литовск, ныне - Брест в Белоруссии), павшем после 
  героического сопротивления. Для Советского Союза эта война обошлась практически 
  без потерь, так как польским войскам на Восточном фронте приказано было не сопротивляться.<br>
