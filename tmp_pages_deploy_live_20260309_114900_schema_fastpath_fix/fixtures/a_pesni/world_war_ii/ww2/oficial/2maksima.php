	<span class="header"><strong>ДВА 
МАКСИМА</strong><br></span>
<br>
Музыка Сигизмунда Каца<br>
Слова Владимира Дыховичного <br>
<br>
На границе шумели березки, <br>
Где теперь пришлось нам воевать,<br>
Там служили-дружили два тезки - <br>
Их обоих Максимами звать. <br>
Был один пулеметчик толковый <br>
(Познакомьтесь с Максимом моим!), <br>
А другой - пулемет был станковый <br>
По прозванию тоже &quot;максим&quot;. <br>
<br>
Крепко связаны дружбою старой, <br>
Принимали грозные бои <br>
Неразлучною, дружною парой <br>
Оба тезки - Максимы мои. <br>
Очень точно наводит наводчик, <br>
А &quot;максим&quot;, словно молния, бьет. <br>
&quot;Так, так, так!&quot; - говорит пулеметчик, <br>
&quot;Так, так, так!&quot; - говорит пулемет. <br>
<br>
От осколка германской гранаты <br>
Не случилось уберечься им: <br>
Пулеметчик был ранен, ребята, <br>
Поврежден пулемет был &quot;максим&quot;. <br>
Дни леченья проносятся мимо, <br>
И дружку произведен ремонт, <br>
И опять оба тезки Максима <br>
Возвращаются вместе на фронт.<br>
<br>
А на фронте – горячий и хлесткий<br>
Ураганный бой гудит опять.<br>
И опять служат-дружат два тезки,<br>
Их обоих Максимами звать.<br>
Снова точно наводит наводчик,<br>
С максимальною силою бьет.<br>
&quot;Так, так, так!&quot; - говорит пулеметчик, <br>
&quot;Так, так, так!&quot; - говорит пулемет. <br>
<br>
 1941<br>
<br>
<em>Священная война. Песни Победы / Сост. В. А. Костров, Г. Н. Красников. М.: Олимп, 2005.</em><br>
<br><br>
<em>Людмила Гурченко вспоминала, что ноты и текст этой песни в числе других им с мамой прислал в Харьков отец-баянист с фронта, особо отметив популярность среди бойцов &quot;Двух Максимов&quot;. Воспоминание относится к зиме 1944-45 гг.:<br>
<br>
Зима сорок пятого года опять была холодной и голодной. Зарплату в училище мама получала небольшую, зато в те дни, когда были занятия кружка, мы обедали и ужинали. Я высчитывала: вторник, четверг, суббота! Во-первых, поедим, а во-вторых, я присутствовала при разучивании новых песен. Большинство этих песен мы получали от папы с фронта. Он вкладывал в письмо нотную строчку и слова песни.<br>
<br>
Мне казалось чудом, что в начале репетиции был немой листочек нот и текста - просто измятый листок, вложенный в треугольнео солдатское письмо, а к концу репетиции возникала живая песня.<br>
<br>
Одну песню папа просил выделить особо: &quot;Лёль! Ета песня на фронте имеить первоклассный успех! Смотри, не проворонь вещь, приподнеси, як следуить быть&quot;. Называлась она &quot;Два Максима&quot;.<br>
<br>
Припев я напевала с утра до вечера:<br>
<br>
&quot;Так-так-так,&quot; - говорит пулеметчик,<br>
&quot;Так-так-так,&quot; - говорит пулемет.<br>
<br>
Первыми песни с фронта исполнили в Харькове участники маминого кружка самодеятельности в ремесленном училище №11.<br>
<br>
А кто прислал эти песни с самого фронта, с самой передовой? Мой папа!</em><br>
<br>
(Гурченко Л.М. Мое взрослое детство. М.: Мол. гвардия, 1982. С. 157).<br>
