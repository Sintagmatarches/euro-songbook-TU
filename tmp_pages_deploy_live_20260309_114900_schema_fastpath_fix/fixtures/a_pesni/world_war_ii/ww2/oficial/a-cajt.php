	 <strong><em>Газета 
  "За рубежом" №36 (1469), 1988 г.</em></strong><br>
<br>
  <em>Вторая мировая война, развязанная фашистской Германией 1 сентября 1939 года, 
  началась с вторжения вермахта в Польшу. Но главной своей задачей, как и прежде, 
  Гитлер считал разгром Советского Союза, завоевание нового "жизненного пространства" 
  для немцев на Востоке вплоть до Урала, несмотря на то, что 23 августа 1939 года 
  был подписан советско-германский договор о ненападении. Не изменились эти планы 
  и после того, как в конце сентября того же года был заключен договор "о дружбе 
  и границе" между СССР и Германией. 22 июня 1941 года Германия напала на нашу 
  страну. <br>
  <br>
  Все это общеизвестные факты. Однако с некоторых пор западногерманские историки 
  и публицисты правого толка публикуют статьи, брошюры и монографии, в которых 
  "по-новому" (а фактически в духе нацистской пропаганды) толкуют основные события 
  войны. Они утверждают, в частности, что нападение Гитлера носило якобы "превентивный 
  характер", поскольку-де от Советского Союза исходила некая "угроза" для рейха. 
  Историки, придерживающиеся марксистских, социал-демократических или либеральных 
  взглядов, на страницах западногерманской прессы дали подкрепленный фактами отпор 
  подобным утверждениям. При этом они справедливо подчеркивали, что попытки ревизовать 
  историю преследуют цель сохранить "образ врага", легенду об "угрозе с Востока", 
  помешать установлению отношений доверия между СССР и ФРГ. <br>
  <br>
  "Дискуссия историков" продолжается уже третий год. Недавно гамбургский еженедельник 
  "Цайт" опубликовал две статьи, написанные сотрудниками Военно-исторического 
  ведомства во Фрайбурге. Любопытно, что три года назад это ведомство, тесно связанное 
  с министерством обороны, выпустило очередной том весьма обширного труда "Германский 
  рейх и вторая мировая война", в некоторых разделах которого проводилась мысль 
  о "превентивном характере" нападения Гитлера на СССР. Как можно убедиться, это 
  не помешало двум сотрудникам ведомства выступить против очевидного искажения 
  истины. <br>
  <br>
  "Цайт" - еженедельная газета, одна из самых влиятельных в ФРГ. Освещает вопросы 
  политики, экономики, торговли и культуры. Основана в 1946 году. Тираж 459,3 
  тысячи. <br>
  </em><br>
  <br>
  <strong>Рольф-Дитер Мюллер, "Цайт", Гамбург</strong><br>
  <br>
  <span class="header"><strong>"РАЗРЕЗАТЬ ПИРОГ НА УДОБНЫЕ КУСКИ"<br>
  Какие цели преследовал Гитлер, нападая на Советский Союз</strong><br></span><br>
  В СССР начинают по-новому, самокритично анализировать и освещать свое прошлое 
  (и в первую очередь сталинскую эпоху), а в ФРГ кое-кто пытается тем временем 
  реидеологизировать историю и оживить прежний "образ врага". Речь идет о попытках 
  не только обелить все то, что творилось в Освенциме, но и затушевать и даже 
  переосмыслить причины германо-советской войны 1941-1945 годов. С точки зрения 
  науки на все эти вопросы давно дан ответ, спорными остаются лишь политические 
  выводы. <br>
  <br>
  Одни (в том числе и президент ФРГ Рихард фон Вайцзеккер в своем выступлении 
  8 мая 1985 года) придерживаются мнения, что нападение Германии на Советский 
  Союз 22 июня 1941 года обязывает нас отказаться от прежних "образов врага" и 
  примириться с народами Советского Союза, а другие видят корень зла в большевистской 
  Октябрьской революции 1917 года. Они утверждают, что действия Гитлера были своего 
  рода "радикальным ответом" на возникшую тогда и существующую по сей день "опасность 
  с Востока". Этот обуреваемый страхом диктатор отдал приказ не только на "окончательное 
  решение еврейского вопроса", но и на превентивную войну против якобы изготовившейся 
  к наступлению Красной Армии. Эту версию Гитлер лично вдалбливал в головы своих 
  солдат на Восточном фронте. Чтобы разоблачить ее, достаточно вспомнить цели, 
  которые "третий рейх" преследовал в войне с СССР, идеи, которые зародились не 
  только в голове диктатора, но и у определенной части руководящей элиты Германии, 
  участвовавшей в их разработке и пропаганде. <br>
  <br>
  <br>
  <strong>ПЛАНЫ СОЗДАНИЯ "ГЕРМАНСКОЙ ИМПЕРИИ НА ВОСТОКЕ" </strong><br>
  <br>
  "ИТАК, главная задача - разрезать этот громадный пирог на удобные куски, чтобы 
  мы могли, во-первых, удержать его в своей власти, во-вторых, управлять им и, 
  в-третьих, эксплуатировать его" - такую запись сделал 16 июля 1941 года Мартин 
  Борман, личный секретарь Гитлера, в протоколе совещания в очень узком кругу, 
  длившегося пять часов. <br>
  <br>
  Уже почти месяц на Востоке свирепствовала война. Бои в приграничном районе проходили 
  по плану. Через несколько недель ожидалось взятие Москвы. "Житница Украина", 
  железная руда и уголь Донбасса, нефть Кавказа - все это казалось вполне доступным, 
  когда Гитлер разъяснял свои политические представления о будущем оккупированных 
  восточных территорий. Его высказывания не оставляют никаких сомнений относительно 
  истинной причины войны. Она объяснялась отнюдь не страхом перед большевистской 
  угрозой, а желанием осуществить давно вынашивавшуюся Гитлером мечту о германской 
  империи на Востоке. <br>
  <br>
  0 необходимости завоевания "жизненного пространства на Востоке" путем разгрома 
  России Гитлер писал еще в своей книге "Майн кампф". Он подхватил давние устремления 
  кайзеровской Германии к экспансии на Востоке, подкрепив их антибольшевизмом 
  и расистской идеологией. В июне 1940 года, когда в результате французской кампании 
  ситуация казалась благоприятной, генералы приступили к разработке соответствующих 
  военных планов. <br>
  <br>
  Цель с самого начала была ясна и без пространных разъяснений диктатора. В 1942 
  году Отто Бройтигам, дипломат старой школы, специалист по России, руководитель 
  идеологического отдела в Восточном министерстве, а после войны - руководитель 
  восточного отдела в министерстве иностранных дел ФРГ, в докладной записке выразил 
  суть проблемы так: <br>
  <br>
  "На Востоке Германия ведет тройную войну: войну на уничтожение большевизма, 
  войну на разгром великорусской империи и, наконец, войну за приобретение колониальных 
  территорий с целью их заселения и экономической эксплуатации". <br>
  <br>
  Военная задача поначалу казалась простой. Как противника Красную Армию всерьез 
  не принимали. Поэтому надеялись на полную свободу действий в оккупированной 
  России, так что можно будет "выпотрошить" страну на потребу вермахту и военной 
  экономике, не считаясь ни с местным населением, ни с международно-правовыми 
  нормами. <br>
  <br>
  Гитлеру оставалось лишь дать свое "благословение". Он поставил во главу угла 
  экономические задачи и поручил их решение военному "экономическому штабу Восток", 
  в котором сотрудничали офицеры вермахта и представители промышленности. Реализация 
  этих целей не позволяла немцам рядиться в тогу "освободителей". Беспощадное 
  насилие должно было вселить в души людей страх и ужас, что позволило бы относительно 
  малыми силами удерживать в повиновении огромную страну. Поэтому Гитлер и дал 
  указание не рассматривать красноармейцев как "таких же солдат-фронтовиков", 
  а вести беспощадную войну на уничтожение, "расстреливая каждого, кто осмелится 
  бросить хотя бы один косой взгляд" (эти слова Гитлер произнес на совещании 16 
  июля 1941 года). "Оправдывать эти действия в глазах всего мира" следовало обычным 
  методом: "Мы снова скажем, что нас вынудили захватить определенную территорию, 
  навести там порядок и обеспечить безопасность... Все необходимые меры - расстрелы, 
  выселение людей и т. д. - мы все равно примем, все равно сможем принять". <br>
  <br>
  Итак, о том, чтобы вермахт, устранив мнимую опасность с Востока, вернулся в 
  родные пределы, и речи не было. Альфред Розенберг, главный идеолог Гитлера и 
  вновь назначенный "рейхсминистр по делам оккупированных восточных территорий", 
  в своих высказываниях пошел еще дальше: "Мы ведем сейчас "крестовый поход" против 
  большевизма не для того, чтобы навеки избавить от большевизма "бедных русских", 
  а для того, чтобы проводить германскую мировую политику и обеспечить безопасность 
  германского рейха... Поэтому война с целью создания неделимой России исключается. 
  Заменить Сталина новым царем, а тем более поставить вместо него какого-нибудь 
  националистического вождя значило бы мобилизовать со временем всю энергию местного 
  населения против нас самих". <br>
  <br>
  <br>
  <strong>РАСЧЕТ НА "МАССОВОЕ ВЫМИРАНИЕ СОВЕТСКОГО НАСЕЛЕНИЯ" </strong><br>
  <br>
  САМЫЕ ВАЖНЫЕ аргументы в поддержку захватнической и истребительной войны сформулировал 
  Герберт Бакке. Этот статс-секретарь министерства продовольствия еще весной 1941 
  года разработал программу, нацеленную на то, чтобы выкачать из России максимум 
  продовольствия и таким образом решить эту проблему в самом рейхе. Немецкое население 
  с недовольством и беспокойством реагировало на все новые сокращения пайков. 
  Гитлер видел в этом подтверждение тому, о чем он неоднократно говорил: обязательно 
  нужна Украина, чтобы Германия снова не оказалась в тисках голода, как это случилось 
  в первую мировую войну в результате блокады. <br>
  <br>
  И Бакке дал внешне вполне деловое оправдание гитлеровским планам войны на истребление. 
  Чтобы в кратчайшие сроки выкачать из России продовольствие, следует, считал 
  он, сократить до минимума потребление местным населением и снять со снабжения 
  "лишних едоков". Эта мера рассматривалась как первый шаг в создании запланированного 
  экономического "нового порядка", направленного против местного рабочего класса 
  и крупных городов: в будущей германской Восточной империи им не было места. 
  <br>
  <br>
  Учитывались ли последствия, которые могло иметь все это для советского населения? 
  Совещание 2 мая 1941 года с участием соответствующих статс-секретарей и генералов 
  не оставляет никаких сомнений в том, что с самого начала хладнокровно планировалась 
  массовая гибель людей: "Войну можно будет продолжать только в том случае, если 
  на третьем году войны весь вермахт будет снабжаться продовольствием за счет 
  России. В результате, конечно, десятки миллионов людей погибнут от голода, если 
  мы возьмем все необходимое для нас в этой стране". <br>
  <br>
  Этим объясняется и директива Гитлера, согласно которой Ленинград и Москва не 
  подлежали оккупации: их надлежало "стереть с лица земли", чтобы не кормить потом 
  население. Военные власти, которым поручалось выполнение этой директивы, не 
  сомневались, что в случае необходимости немецкие солдаты будут стрелять в голодающих 
  людей, но считали более целесообразным держать открытым коридор, по которому 
  можно было бы переправить население за линию фронта противника. <br>
  <br>
  Надежды Гитлера и Геринга на то, что ближайшей зимой в России разразится "крупнейший 
  в мировой истории голод", основывались отнюдь не на трудностях регулярного снабжения 
  в условиях войны, а на явном стремлении высшего нацистского руководства истребить 
  советское население. Среди представителей властей более мелкого ранга в 1941 
  году тоже широко бытовало мнение, что на Востоке явный "избыток людей" и что 
  внимания заслуживает лишь та часть населения, которая работает на немцев. В 
  пояснении к одной служебной инструкции для работников трудового управления говорится: 
  рабочим придется еще долго расплачиваться за то, что они терпели большевистский 
  режим. <br>
  <br>
  Массовое вымирание советского населения входило в расчеты не только оккупационных 
  властей. Оно послужило и отправной точкой для разработки "генерального плана 
  Ост". В нем речь шла о детальной тридцатилетней программе заселения будущих 
  восточных территорий, "германизации" территории между Вислой и Уралом. В планах 
  ведомства Гиммлера советскому населению отводился статус рабов на службе у "германских 
  крестьян-колонистов". На первых порах численность советского населения должна 
  была сократиться на 31 миллион человек. 14 миллионов людей "хорошей породы", 
  которых собирались сначала использовать в качестве рабочей силы, подлежали содержанию 
  в резервациях, а затем выселению. Так складывалась программа гигантского геноцида, 
  включавшая и истребление евреев. <br>
  <br>
  Бредовая идея создания империи на Востоке породила у германской элиты в 1941 
  году неуемную страсть к различного рода планам. Промышленная бюрократия, руководители 
  концернов и экономисты стали подумывать о создании экономики на территории "большой 
  Европы". Подразумевалась территория от Атлантики до Урала, от Нордкапа до некоторых 
  районов Африки. На всей этой территории германская экономика должна была получить 
  безграничные возможности для экспансии. Уже разгорались первые схватки за зоны 
  влияния. Комиссии и эксперты так называемых восточных компаний набросились на 
  оккупированные территории, чтобы взять на учет или просто присвоить те или иные 
  промышленные предприятия. Правда, пока шла война, германскую экономику и ее 
  "дранг нах Остен" несколько сковывала гитлеровская директива, согласно которой 
  при дележе военных трофеев предпочтение следовало отдавать фронтовикам. <br>
  <br>
  Болезненное увлечение планами охватило и командование вермахта. Возможность 
  иметь гигантские учебные полигоны на территории России, установить военную границу 
  по Уралу, откуда время от времени можно было бы проводить силами танковых корпусов 
  рейды в азиатскую часть России, - все это открывало небывалые перспективы для 
  роста вермахта. Военно-морские силы разрабатывали обширную программу строительства 
  новых кораблей. Сухопутные войска собирались в течение трех лет увеличить число 
  своих танков в десять раз. Геринг приказал увеличить ВВС в четыре раза. Для 
  этого требовались материальные ресурсы России, но не было нужды в ее людском 
  потенциале, пригодиться могла разве что его незначительная часть. <br>
  <br>
  Может быть, именно этим объясняется ужасающее равнодушие командования вермахта 
  к судьбам советских военнопленных, этой миллионной армии отчаявшихся людей, 
  сдавшихся на милость вермахта? Они напрасно надеялись на соблюдение правил "рыцарского 
  ведения войны". В конечном итоге пленные оказались на положении ненужного балласта 
  бесполезного "человеческого материала", обреченного на лишения и голодную смерть 
  в пути следования и в примитивных лагерях. Сотни тысяч пленных были ликвидированы 
  по политическим и расистским соображениям, бесчисленное множество - забиты до 
  смерти или расстреляны на дорогах только потому, что, обессилев, они не могли 
  двигаться дальше или проявляли хотя бы тень строптивости. В условиях победной 
  эйфории сомнения, бытовавшие и в вермахте, не смогли проявиться в полную силу. 
  <br>
  <br>
  В ходе тщательно фиксировавшихся бесед за столом или у камина Гитлер позволял 
  себе помечтать. В будущих восточных колониях, вещал он, "имперский крестьянин" 
  будет "жить в исключительно красивых поселениях. Немецкие учреждения и органы 
  власти должны иметь великолепные помещения, а губернаторы - настоящие дворцы". 
  Вокруг следовало размещать резервации славянских "гелотов" (рабов), в случае 
  восстания будет достаточно сбросить на них "парочку бомб". В будущем предполагалось 
  раз в год проводить по имперской столице "колонну киргизов", дабы "мощь и величие 
  ее каменных памятников поразили их воображение". Эти фантастические картины 
  захватывали Гитлера с такой силой, что он переходил на крик: "Если бы я мог 
  внушить немецкому народу, какое значение имеет этот край для нашего будущего! 
  Это наша Индия, и так же, как англичане с помощью горстки людей владеют ею, 
  так и мы будем управлять этой нашей колонией... Страна, которую мы сейчас осваиваем, 
  послужит нам источником сырья и рынком сбыта, а не средоточием промышленного 
  производства". <br>
  <br>
  "Туземное", то есть славянское, население обрекалось на жалкое существование, 
  его культура должна была удерживаться на максимально низком уровне, а численность 
  сокращаться по мере нарастания темпов переселенческой кампании. Считалось, что 
  создавать хорошо продуманную систему управления не стоит, что можно ограничиться 
  назначением комиссаров-надзирателей, готовых в любой момент пустить в дело пистолет. 
  Умение русских "недочеловеков" читать и писать рассматривалось как нечто вредное. 
  "Хватит и знания дорожных знаков", - говорил Гитлер. В каждой деревне надо установить 
  по одному громкоговорителю и целый день подбадривать людей веселой музыкой. 
  Ни в коем случае не следовало заниматься гигиеной и охраной здоровья местного 
  населения. "Наших юристов и врачей придется, видимо, сдерживать силой: никаких 
  прививок, никаких помывок! Давать им только водку и табак, сколько душе угодно". 
  <br>
  <br>
  Диктатор намеревался собрать со всего света "арийских" переселенцев и с их помощью 
  германизировать этот "дикий Восток". Он считал, что для этого придется построить 
  скоростные автострады и крупные железнодорожные магистрали. Может быть, это 
  были всего лишь "грезы у камина", фантазии диктатора, страдающего манией величия 
  и позволяющего себе помечтать за чашкой чая с печеньем в кругу подобострастно 
  внемлющих секретарш и лакеев? Отнюдь нет: имперские железные дороги приступили 
  к разметке будущих перегонов, специалисты по сельскому хозяйству начали подготовку 
  первых переселенцев, а карательные отряды работали без передышки. <br>
  <br>
  <br>
  <strong>"НОВОЕ ЖИЗНЕННОЕ ПРОСТРАНСТВО" ДЛЯ НЕМЕЦКИХ ФЕРМЕРОВ И ПРОМЫШЛЕННИКОВ 
  </strong><br>
  <br>
  НО ВСКОРЕ стало ясно, что чем дольше длится война на Востоке, тем в большее 
  противоречие приходит подобная политика с интересами немецкого военного командования. 
  Стали раздаваться предостерегающие голоса, радикальный курс подвергся критике 
  изнутри. Генерал-лейтенант Ганс Лейкауф, инспектор военной промышленности на 
  Украине, писал в декабре 1941 года: "Если мы и впредь будем расстреливать евреев, 
  обрекать на гибель военнопленных, дадим умереть голодной смертью значительной 
  части населения крупных городов, а в будущем году потеряем от голода и часть 
  сельского населения, то не сможем ответить на вопрос: кто же должен производить 
  здесь материальные ценности". К тому же периоду относятся и слова главнокомандующего 
  9-й армией: "Если бы русская кампания была простым блицкригом, то нам не пришлось 
  бы считаться с гражданским населением. Но войне пока не видно конца... В этих 
  условиях неразумно придерживаться курса, который превращает все сто процентов 
  гражданского населения в наших врагов". <br>
  <br>
  Однако Гитлер не считал возможным изменить свой курс, "война за жизненное пространство" 
  была для него важнее всего. Ведь речь шла не только о каких-то неопределенных 
  идеях на будущее, которые в случае надобности могли бы и подождать, а о решении 
  насущных проблем нацистского государства. В результате стремительного роста 
  вооружений в 30-е годы существенно обострились внутренние социальные и экономические 
  противоречия. <br>
  <br>
  Расходы на вооружения и войну покрывались за счет дополнительного выпуска денежных 
  знаков, уже в 1941 году финансы страны находились на грани краха. Растущая инфляция 
  породила спекуляцию акциями, "черный рынок" и коррупцию. Чтобы по возможности 
  уменьшить бремя войны для собственного населения, следовало снова пополнить 
  кассы рейха и дать промышленности столь необходимые для нее ресурсы, ограбив 
  чужие территории. В банковских кругах это называли "крупнейшей за всю предыдущую 
  историю экономики программой амортизации". <br>
  <br>
  Среднее сословие, важнейшая социальная база национал-социализма, здорово пострадало 
  в годы войны в результате закрытия мелких и средних предприятий и требовало 
  обещанных благодеяний. По данным службы безопасности, уже в мае 1941 года отмечались 
  случаи открытого протеста и угроз, выразившихся в нежелании идти на дополнительные 
  жертвы в будущем. В письме руководителю имперского союза ремесленников Гиммлер 
  попытался смягчить их недовольство, пообещав хорошую перспективу на будущее. 
  "Новое жизненное пространство на Востоке", писал он, открывает сферу деятельности, 
  "обширнее и заманчивее которой еще не было в немецкой истории". Создание "здорового" 
  среднего сословия из числа зажиточных крестьян, ремесленников и кустарей - именно 
  эту модель общества предстояло опробовать на Востоке, а затем распространить 
  и на сам рейх. <br>
  <br>
  На оккупированной территории уже появлялись новые рабочие места. Множество штурмовиков 
  поспешили окопаться на хорошо оплачиваемых должностях в гражданской администрации. 
  В Россию послали свыше десяти тысяч фермеров, они должны были наладить хозяйство 
  на огромных наделах, причем нередко в одиночку, и контролировать сельское население. 
  Возможность получить "дворянское поместье на Востоке" окрыляла немало немцев. 
  Об этом мечтал даже такой человек, как Герхарт Гауптман (известный писатель, 
  в 1912 году удостоен Нобелевской премии. - Ред.), представитель старого бюргерства: 
  "Земельный голод. Россия. Игра стоит свеч. Великолепные уголки Кавказа: блаженство 
  нового немецкого заселения. Переселение народов на Юго-Восток. Украина". <br>
  <br>
  Нацистская партия стремилась сделать захватническую войну привлекательной и 
  для рабочего. В бесчисленных брошюрах и лекциях ему рисовали картину превращения 
  из "пролетария в господина". Разрабатывались программы жилищного строительства, 
  улучшения социального обеспечения и системы оплаты труда. По указанию Гитлера 
  эти прожекты держались наготове в ящиках письменных столов, чтобы после оконча- 
  тельной победы рабочий класс смог получить свою долю военных трофеев. А пока 
  рабочий должен был быть благодарным солдату, ибо тот защищал "Европу от еврейского 
  чудовища". "Немецкие солдаты защищают не только Германию, все народы со временем 
  будут благодарны за то. что Адольф Гитлер создал немецкий вермахт, чтобы освободить 
  Европу от щупальцев международного еврейского спрута", - писал Роберт Лей, фюрер 
  "трудового фронта". <br>
  <br>
  Солдатам уже тогда обещали материальное вознаграждение. В листовках рекламировались 
  будущие поселения на Востоке, верховное командование вермахта назначило специального 
  "уполномоченного вермахта по вопросам заселения" оккупированных территорий. 
  Полковник Борнхаупт вместе с СС готовил планы расселения участников войны. Возрождалась 
  старая традиция, ведь еще в 1918-1919 годах тысячи людей завербовались и стали 
  солдатами добровольческих корпусов, так как им обещали поселение на Балтике. 
  Эти бывшие солдаты служили теперь в вермахте, в основном это были пожилые офицеры 
  в комендатурах, полицейских частях и других тыловых службах. <br>
  <br>
  Фронтовым частям жестокие бои не оставляли времени для подобных мечтаний о будущих 
  поселениях. Но сама идея была знакома и им. Правда, после первой зимы возобладало 
  одно желание: не попадать больше в Россию. Это отмечалось в донесении командования 
  3-й танковой армии о настроениях среди солдат. Но позже, когда летом 1942 года 
  снова началось наступление, нацистская пропаганда развернулась вовсю. Геббельс 
  громогласно заявил, что война ведется не ради "трона и алтаря", не за идеалы, 
  а "за зерно и хлеб, за хорошо накрытый к завтраку, обеду и ужину стол, за создание 
  материальных предпосылок для решения социальных вопросов, вопросов жилищного 
  и дорожного строительства, за создание военного, торгового и туристского флота, 
  за производство народных автомобилей и тракторов, за строительство театров и 
  кинотеатров для народа в каждой, даже самой захудалой деревне, идет война за 
  сырье". <br>
  <br>
  Видимо, многие солдаты усвоили это. В уже упоминавшемся донесении о настроениях 
  в армии отмечалось, что после главной темы разговоров - о верности жен - солдат 
  больше всего интересует вопрос об их профессиональной судьбе после войны. "Все 
  чаще возникает вопрос об условиях жизни на Востоке после войны... Командиры 
  двух батальонов мечтают о том, как они со временем превратят нынешние участки 
  обороны своих батальонов в два соседних поместья, причем об этом говорилось 
  не в шутку, а всерьез". Подобные зачатки "растущей готовности поселиться на 
  Востоке" надлежало поощрять всеми средствами, дабы поднять стойкость солдат. 
  <br>
  <br>
  <br>
  <strong>СЛАВЯНАМ ГОТОВИЛИ УЧАСТЬ "БЕЛЫХ НЕГРОВ" </strong><br>
  <br>
  ОДНАКО вскоре Розенбергу пришлось призвать все высшие власти рейха к сдержанности. 
  Это произошло в начале 1943 года, когда 6-я армия истекала кровью под Сталинградом. 
  По его мнению, разглагольствования о том, что Германия собирается проводить 
  на Востоке колониальную политику, эксплуатировать местных жителей и возводить 
  свои поселения, используются советской пропагандой для укрепления воли к сопротивлению. 
  Но эту страсть к планам было уже трудно охладить, поэтому летом 1943 года Гитлеру 
  пришлось в секретном предписании умерить чересчур навязчивые притязания представителей 
  партийного аппарата и вермахта на получение земельных владений на Востоке. <br>
  <br>
  И все же планы заселения Востока не приобрели характер массового психоза. Трудности 
  военных лет на фронте и в тылу быстро заставили забыть разговоры о целях войны, 
  пропаганда сочла более целесообразным ограничиться всяческим раздуванием "опасности 
  с Востока". <br>
  <br>
  Пропаганда теории "недочеловека" и идеи заселения Востока, всяческое насаждение 
  "образа врага" принесли свои плоды хотя бы в одной области: человеческие отношения 
  между немцами и русскими почти полностью исключались. В тылу, где бушевала беспощадная 
  партизанская война, население целых районов становилось жертвой жестоких карательных 
  экспедиций немецкой армии. Не были исключением и беззастенчивые грабежи со стороны 
  немецких воинских частей, оставленных командованием без достаточного снабжения. 
  <br>
  <br>
  Видимо, нередки были и перегибы в духе теории "сверхчеловека", а также коррупция 
  среди оккупационных властей. В начале 1942 года Розенбергу пришлось официально 
  запретить широко практиковавшиеся телесные наказания - к большому сожалению 
  чиновников от сельского хозяйства, считавших, что иначе нельзя заставить русских 
  работать. (Позднее договорились, что телесные наказания будут проводиться пособниками 
  из числа местных жителей.) К местным жителям - будь то крестьянин или профессор 
  - обращались только на "ты". Сами они чувствовали себя "белыми неграми немцев". 
  Советские граждане, военнопленные и лица, отправленные в Германию на принудительный 
  труд у станков, на полях и в шахтах в качестве так называемых восточных рабочих 
  (в основном это были женщины и дети), редко встречали человеческое обращение. 
  Их положение вряд ли было лучше положения евреев. Лишь в марте 1945 года дискриминационные 
  правила были отменены. Эти люди выжили лишь потому, что нацисты остро нуждались 
  в рабочей силе. Занятые подневольным трудом восточные рабочие отнюдь не были 
  "союзниками" в борьбе с большевизмом, как пыталась представить их нацистская 
  пропаганда на заключительном этапе войны. <br>
  <br>
  Сотни тысяч людей, угнанных вермахтом в ходе отступления в 1943-1944 годах, 
  лишились своего имущества, их детей заставляли выполнять подсобные работы на 
  зенитных батареях или помещали в специальные детские лагеря. Эти люди на собственном 
  опыте убедились, что немцы воюют не только с коммунистами, но и со своими славянскими 
  соседями в целом. Поэтому наступательная и захватническая война 1941 года не 
  могла автоматически превращаться в справедливую, национальную и оборонительную 
  войну по мере отступления вермахта и продвижения Красной Армии. <br>
  <br>
