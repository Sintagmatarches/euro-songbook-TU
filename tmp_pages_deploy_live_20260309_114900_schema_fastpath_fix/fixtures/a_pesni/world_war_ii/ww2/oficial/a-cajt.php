  заключенного со Сталиным договора о дружбе отвела свои войска с восточной границы, 
  однако "в то же самое время началось развертывание русских сил в таком масштабе, 
  что это могло быть расценено только как сознательная угроза Германии". Далее 
  он утверждал - и это солдаты слышали от своих командиров, - что "как ответственный 
  вождь германского государства и ответственный представитель европейской культуры 
  и цивилизации" пытался прийти к примирению с Советским Союзом путем переговоров. 
  Но якобы Москва развертыванием своих армий нарушила положения договора о дружбе, 
  совершив "подлое предательство": "Сегодня на нашей границе стоят 150 русских 
  дивизий. Уже несколько недель происходят длительные нарушения этой границы... 
  Однако теперь пришел час, когда необходимо выступить против этого сговора еврейско-англосаксонских 
  поджигателей войны и таких же еврейских правителей большевистской Москвы". По 
  словам Гитлера, открытие нового фронта на Востоке осуществлялось не только для 
  того, чтобы "создать предпосылки для окончательного завершения большой войны 
  вообще или защитить вовлеченные страны, но и для того, чтобы спасти всю европейскую 
  цивилизацию и культуру". Таким образом, в солдатские руки якобы вкладывалась 
  "судьба Европы, будущее германского государства, судьба нашего народа". <br>
  <br>
  В очень популярном тогда "недельном обозрении" (еженедельные выпуски кинохроники. 
  - Ред.) сотрудники пропаганды вермахта в последующие дни и недели неизменно 
  демонстрировали кадры, показывающие советские войска и оснащение Красной Армии, 
  дававшие якобы возможность убедиться, насколько велика была опасность, исходящая 
  с Востока. Делались также ссылки на высказывания советских офицеров о предпринимаемой 
  "подготовке наступления". <br>
  <br>
  7 июля 1941 года Геббельс прочел в донесении службы безопасности: "Согласно 
  сообщениям, повсеместно с успехом распространяется мысль о том, что Советский 
  Союз представлял острую опасность для рейха и что фюрер снова нанес удар в нужный 
  момент". Таким образом, как и намечалось, мысль о "превентивной войне" внедрялась 
  в умы людей. Геббельс, однако, сознавал, что успех данной риторики зависел от 
  военно-политической обстановки. На другой день после нападения в его дневнике 
  появилась примечательная запись; "Если мы победим, то мы правы". <br>
  <br>
  В национал-социалистской пропаганде такие слова, как "охрана", "оборона", "защита", 
  употреблялись не в традиционном значении военной защиты государственных границ. 
  Классическая идея обороны страны, признававшаяся, как известно, и либералами, 
  и социалистами, и умеренными пацифистами как естественное право на коллективную 
  защиту, систематически искажалась нацистским режимом для последовательной маскировки 
  собственных агрессивных намерений, выдаваемых за оборонительные. Составная часть 
  этой извращенной трактовки оборонного тезиса трансформировалась впоследствии 
  в идею "крестового похода Европы против большевизма" под германским руководством, 
  то есть самую широкомасштабную захватническую программу. <br>
  <br>
  Если следовать пропагандистской риторике "третьего рейха", то получается, будто 
  немецкие солдаты неизменно лишь "вели ответный огонь". Так было и в сентябре 
  1939 года, когда инсценировали нападение германских подданных в польской униформе 
  на радиостанцию в верхнесилезском промышленном городе Глейвиц, чтобы иметь возможность 
  представить собственные агрессивные действия, с помощью которых и была развязана 
  вторая мировая война, в виде оборонительных мер. <br>
  <br>
  <br>
  <strong>УРОКИ ИСТОРИИ </strong><br>
  <br>
  ВОЗНИКАЕТ вопрос, почему в общем-то не слишком щепетильные представители нацистского 
  режима приложили столько усилий для перекладывания ответственности за развязывание 
  войны на других. Здесь следует учесть два аспекта. Во-первых, хотя война в то 
  время и считалась законным средством осуществления политики, однако в общественном 
  сознании оправданной признавалась лишь оборонительная война - не агрессивная. 
  Со времени заключения в 1928 году так называемого Парижского пакта, связанного 
  с именами таких политиков, как Келлог (в то время - государственный секретарь 
  США. - Ред.), Штреземан (министр иностранных дел Германии. - Ред.) и Бриан (министр 
  иностранных дел Франции. - Ред.), агрессивная война была поставлена вне закона 
  и международным правом. Однако нарушение международного права не испугало бы 
  деятелей нацистского режима. Куда весомее была - и в этом заключается второй 
  аспект - опасность того, что признание собственной агрессии осложнит психологическую 
  мобилизацию солдат вермахта и союзников. Кроме того, могла, пожалуй, иметь значение 
  и боязнь сопротивления, несмотря на внешне успешную политику репрессий. <br>
  <br>
  В ходе войны с Россией слово "защита" потеряло всякое оборонительное содержание. 
  Оно превратилось в пропагандистское понятие для оправдания именно той войны, 
  которую вел германский рейх, - захватнической и истребительной. <br>
  <br>
  Черно-белому мышлению соответствовало и распределение ролей: с одной стороны, 
  "силы разрушения", то есть Советский Союз, с другой - силы "обновления". "Праведная" 
  сторона, то есть германский рейх, вела агрессивную войну в виде "оборонительной 
  борьбы" под фантастическим предлогом "защиты всего, что сделало Европу центром 
  человеческой культуры и морали в тысячелетних боях, трудах и творческих достижениях". 
  Большевизм выставлялся "организованным безбожием", в то время как самим себе 
  приписывали благородную миссию защитников Запада. <br>
  <br>
  Таковы источники, в значительной мере питающие обывательскую психологию, с одной 
  стороны, и с другой - красивенькие легенды всех цветов и оттенков о превентивной 
  войне. Кое-кому из воевавших они по сей день служат либо для морального оправдания 
  собственной роли в прошедшей войне, либо для успокоения чувства вины, либо как 
  средство отделаться от неприятной правды. К этому можно даже отнестись с некоторым 
  пониманием. Ведь миллионы немцев, которые солдатами или в иной роли - частью 
  по принуждению, частью добровольно, частью соглашаясь, частью отрицая, - участвовали 
  в этой войне, боясь ее и страдая, возможно, не меньше, чем их противник, пожалуй, 
  искали и нашли в этих оборонительных аргументах по меньшей мере определенную 
  психологическую опору. <br>
  <br>
  Да и позднее, после войны, сознание собственного участия в агрессивной войне, 
  создавшей к тому же предпосылки для осуществления чудовищных истребительных 
  акций, было тяжким бременем, которое пытались хоть немного облегчить ссылкой 
  на мнимую защиту "народа и отечества". И как же тяжело было сознавать, что лучшие 
  годы жизни были разрушительными, бессмысленными и греховными! Оборонительная 
  аргументация, прославляющая "западный героизм" в борьбе с азиатским "недочеловечеством", 
  привела к политической и общечеловеческой дезориентации. <br>
  <br>
  Агрессивные войны нацистской Германии и сопровождавшая их оборонительная ложь, 
  а также, возможно, и другие сопоставимые периоды немецкой - и международной 
  - истории не прошли бесследно, оставив после себя дефицит доверия, а то и полное 
  недоверие. <br>
  <br>
  Знали об этом и "отцы" нашей конституции. Они попытались отреагировать на исторический 
  опыт, возведя правовые барьеры. Статья 26-я Основного закона гласит: "Действия, 
  способные нарушить мирную совместную жизнь народов и предпринимаемые с этой 
  целью, в частности для подготовки агрессивной войны, являются противоконституционными". 
  Но как на практике осуществить эти нормы? Есть ли критерии для четкого разграничения 
  - какие действия с какой целью предпринимаются? <br>
  <br>
  Сегодня у нас - как, кстати, и в Советском Союзе, - размышляют над тем, как 
  с помощью практических мер сделать заслуживающей доверия многократно декларированную 
  политическую волю; не допускать ничего иного, кроме защиты собственной страны. 
  Здесь вступает в дело концепция "структурной неспособности к нападению", призванная 
  оказать политическое воздействие, ликвидировать страх и установить доверие. 
  В наше время, когда большинство людей (в том числе и в нашем регионе) хотя и 
  не имеют ничего против политических деклараций об отказе от применения силы, 
  но одновременно не слишком верят в возможность отказа от оружия, якобы необходимого 
  хотя бы в целях "сдерживания", перестройка военной организации таким образом, 
  чтобы практически исключить злоупотребления прошлых времен, могла бы оказаться 
  решающей для продвижения к главной цели - всеобщему и полному запрещению войн. 
