  ОБЩЕЕ объяснение этому можно, пожалуй, искать в самом понятии обороны. Оно, 
  как известно, употреблялось и употребляется в двояком смысле: во-первых, в политическом 
  - для оправдания собственных действий против агрессора и, во-вторых, - в чисто 
  военном. Но это еще не все. Издавна политики разных стран, выходя за рамки строгой 
  терминологии, прибегали к более или менее явной "оборонительной лжи", чтобы 
  замаскировать "дипломатическим путем" защиту собственных интересов в широчайшем 
  смысле, в том числе и путем агрессивных действий. К подобному извращению понятия 
  обороны относится и обособление его от понятия защиты собственной государственной 
  территории, и объявление "обороной" даже защиту своих интересов в удаленных 
  частях мира. Еще отчетливее становится проблема, если вспомнить, что ни Лиге 
  Наций, ни ООН не удалось дать определение понятия, противоположного обороне, 
  то есть агрессивной войны. <br>
  <br>
  И в прусско-германской истории есть агрессивной войны. И в прусско-германской 
  истории можно найти множество доказательств произвольного обращения со словом 
  "оборона" и его использования в пропагандистских целях. Так, в 1740 году Фридрих 
  II отдал своей армии приказ о наступлении на Силезию, а чуть позже поручил графу 
  Генриху фон Подевильсу подыскать "justa causa", то есть "справедливое основание", 
  оправдательный мотив, который соответствовал бы господствующему в то время учению 
  о войне и не препятствовал агрессивной военной политике прусского короля. В 
  1871 году Бисмарк, манипулируя "ямской депешей", представил французского императора 
  Наполеона III в роли агрессора, хотя сам нуждался в войне против Франции как 
  средстве создания прусско-германского государства и действовал в этом направлении. 
  <br>
  <br>
  Бетман-Гольвег, канцлер при императоре Вильгельме II, во время июльского кризиса 
  1914 года своими ловкими действиями сумел создать впечатление, будто Германии 
  не оставалось ничего другого, как отреагировать на русскую всеобщую мобилизацию 
  и обороняться. Важнейшим внутриполитическим итогом этой манипуляции было то, 
  что социал-демократия усмотрела необходимость рассматривать оборонительную ситуацию 
  как реальность и в интересах защиты отечества заключить "классовый мир" с правительством. 
  Было отчего возрадоваться главе военно-морского ведомства адмиралу Георгу фон 
  Мюллеру: "Настроение блестящее. Правительству удалось представить нас жертвами 
  нападения!"<br>
  <br>
