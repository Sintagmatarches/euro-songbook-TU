	<strong>Марк 
  Штейнберг</strong><br>
  <br>
  <span class="header"><strong>СОВЕТСКИЙ СПЕЦНАЗ - ВЗЛЕТЫ И ТРАГЕДИИ</strong><br></span> 
  <strong>Очерк второй </strong><br>
  <br>
  <em>"Международная еврейская газета". 2005. Декабрь. №47-48 (542-543).</em><br>
  <br>
  <br>
  <strong>Война</strong><br>
  <br>
  За первые три недели после вторжения в СССР вермахт и его союзники продвинулись 
  более чем на 500 км, полностью уничтожив 170 дивизий Красной Армии, три с половиной 
  тысячи самолетов, выведя из строя около 6 тыс. танков. За первый же месяц войны 
  в плену оказались не менее миллиона бойцов и командиров. Таким образом, кадровая 
  Красная Армия в сущности перестала существовать. <br>
  <br>
  Большую роль в этом сыграли успешные действия германского спецназа - полка "Бранденбург" 
  и других разведывательно-диверсионных формирований. Их массовая заброска началась 
  еще до вторжения; они уничтожали линии проводной связи, выводили из строя мосты, 
  взрывали склады, нападали на штабы, убивали командиров и красноармейцев. Это 
  деморализовало советские войска, сеяло панику в подразделениях и существенно 
  осложняло оперативную обстановку, создавая неразбериху в прифронтовой полосе. 
  <br>
  <br>
  Напротив, советский спецназ в первый период войны, казалось, бездействовал, 
  а на самом деле к тому времени его практически не существовало. Часть армейских 
  диверсантов была расстреляна, часть погибала в Сибири. Намеченные на случай 
  германского вторжения мероприятия не были выполнены, отбор и подготовка людей 
  для проведения диверсионно-разведывательных действий не проводились, базы оружия 
  и боеприпасов не закладывались, радиосвязь не организовывалась. Как уже говорилось 
  в первом очерке, все, что создавалось до сталинских репрессий, было почти полностью 
  уничтожено органами НКВД. <br>
  <br>
  Правда, за две недели до германского вторжения, разведотделы армий на западной 
  границе на свой страх и риск создали и вооружили около 60 групп спецназа, которые 
  после отхода советских войск оставались в тылу противника. Но почти все они 
  не имели средств радиосвязи, действовали непрофессионально и были раскрыты и 
  уничтожены немцами. <br>
  <br>
  Однако обстановка настоятельно требовала организации таких групп и заброски 
  их в тыл противника, поэтому фронтовые разведотделы в спешном порядке формировали 
  диверсионно-разведывательные подразделения. К примеру, на Западном фронте за 
  первые два месяца войны были созданы и заброшены на территорию врага 52 группы 
  спецназа. Примерно столько же заброшено разведотделами других фронтов. <br>
  <br>
  Но из-за отсутствия подготовленных командиров, поспешности в подготовке (от 
  трех до десяти суток), некачественного отбора личного состава, слабого материально-технического 
  обеспечения подавляющее большинство разведывательно-диверсионных групп поставленные 
  перед ними задачи не выполнили, понесли большие неоправданные потери. Уцелевшие 
  в этот период после одной-двух диверсий вынуждены были в дальнейшем заниматься 
  лишь собственным выживанием. <br>
  <br>
  Еще большие неудачи были у так называемых диверсионных полков, сформированных 
  на многих фронтах. В их состав включались подразделения общей численностью свыше 
  тысячи бойцов. Естественно, значительная часть из них была совершенно не пригодна 
  для диверсионной деятельности. Среди этого контингента встречались и морально 
  нестойкие, и просто предатели. <br>
  <br>
  Между тем командование фронтов надеялось на то, что заброшенные диверсионные 
  группы нанесут противнику большой урон: вышло все с точностью до наоборот. Громоздкие 
  формирования было очень сложно забрасывать в тыл противника незаметно для него, 
  и они, как правило, несли значительные потери еще в процессе заброски, а то 
  и полностью уничтожались немцами, даже не достигнув района своего оперативного 
  предназначения. Только после гибели нескольких тысяч "скороспелых" спецназовцев 
  в Разведуправлении Красной Армии отказались от этой губительной практики. <br>
  <br>
  Впрочем, часть диверсионных полков, стихийно распавшись на мелкие группы, стала 
  основой для создания партизанских отрядов. В немецком тылу осенью и зимой 1941 
  года находилось множество бойцов и командиров из разгромленных соединений, не 
  сумевших вырваться из окружения или сбежавших из плена, и других людей, стремившихся 
  к борьбе с оккупантами. Вот они-то и становились партизанами, объединившись 
  вокруг ядра из бывших диверсантов. <br>
  <br>
  Как правило, именно такие партизанские отряды действовали наиболее профессионально, 
  нанося германским войскам, частям их союзников и полицейским формированиям из 
  местных жителей ощутимые удары, заставляя противника выделять для охраны армейских 
  тыловых районов все большее количество войск. И хотя партизаны и диверсионные 
  группы далеко не всегда действовали достаточно эффективно, тем не менее уже 
  к концу 1941 года германское командование было вынуждено направить на борьбу 
  с ними свыше 70 пехотных и полицейских батальонов, кроме спецотрядов службы 
  безопасности СД. <br>
  <br>
  Только к лету 1942 года Разведуправление Красной Армии перестало применять в 
  подготовке диверсионно-разведывательных групп принцип "числом поболее, ценою 
  подешевле". Теперь эти группы готовили разведотделы фронтов, лучше знавшие обстановку 
  и имевшие возможность грамотно отобрать будущих диверсантов. Их выбирали, как 
  правило, из войсковых разведчиков, готовили в течение нескольких месяцев, сравнительно 
  неплохо вооружали, а с осени 1942 года каждая группа получала переносную рацию 
  типа "Северок". Эти мероприятия привели к повышению эффективности действий диверсантов 
  и, что весьма показательно, - снижению их потерь. Если в первый год войны из 
  десяти заброшенных диверсантов погибали не менее семи, то к концу 1942 года 
  эта пропорция составляла из десяти - трое. <br>
  <br>
  К этому времени были освоены наземный, воздушный и водный пути заброски разведывательно-диверсионных 
  групп во вражеский тыл. В случае отступления такие группы просто оставлялись 
  на заранее подготовленных базах, как правило - в лесных массивах. В тактику 
  действий разведывательно-диверсионных групп входили засады, налеты и диверсии 
  при помощи взрыва или поджога. Но наиболее эффективными были рейды по вражеским 
  тылам, в которых использовались все виды атак и диверсий. <br>
  <br>
  Примером удачного рейда могут послужить действия разведывательно-диверсионной 
  группы, составленной из преподавателей и студентов Ленинградского института 
  физкультуры и прошедшей подготовку при штабе Северо-Западного фронта. Группа 
  за два месяца провела 24 операции, уничтожив десятки эшелонов и сотни автомашин, 
  истребив более 400 гитлеровцев и их местных пособников. Однако так успешно действовали 
  далеко не все диверсионные группы Красной Армии, потому что настоящие диверсанты 
  - это профессионалы, прошедшие длительную подготовку и имеющие большой опыт 
  действий, а таких в армейском спецназе в годы войны почти не было. <br>
  <br>
  Только к концу 1943 года на базе Высшей школы Генштаба РККА открылись Центральные 
  диверсионно-разведывательные курсы, готовившие специалистов армейского спецназа. 
  Но и здесь увлекались прежде всего количеством, а не качеством, выпуская каждые 
  четыре месяца по 500 человек. Такие же курсы создавались разведотделами фронтов. 
  Кроме того, в Москве функционировала Центральная школа, готовившая командиров 
  групп и отрядов спецназа. Так что к концу 1944 года число разведчиков-диверсантов 
  в Красной Армии составляло не менее 5-7 тысяч. Для сравнения: в германском вермахте 
  имелся только один диверсионный полк "Бранденбург", численностью до двух тысяч 
  солдат и офицеров, достаточно успешно справлявшийся с многочисленными задачами 
  на разных фронтах Европы. <br>
  <br>
  В первые дни войны в НКВД тоже было создано подразделение для подготовки и заброски 
  в тыл противника диверсионных формирований - Особая группа, переименованная 
  в октябре 1941 года во 2-й отдел НКВД. И Особую группу, и 2-й отдел возглавлял 
  всю войну генерал Павел Судоплатов. Непосредственно же за диверсионную работу 
  отвечал генерал Натан Эйтингон. Символично, что многие сотрудники этих органов 
  были извлечены из лубянских подвалов и сибирских лагерей. В их числе такой опытнейший 
  диверсант, как Яков Серебрянский, приговоренный к смертной казни уже в дни войны 
  и по чистой случайности не расстрелянный. Ему, впрочем, пришлось приходить в 
  себя после зверских пыток более двух месяцев. <br>
  <br>
  Уже в начале осени 1941 года под Москвой была развернута своеобразная школа 
  по подготовке диверсантов, так называемая ОМСБОН (Отдельная мотострелковая бригада 
  особого назначения). Численность ее доходила временами до нескольких тысяч человек, 
  в том числе - почти две тысячи иностранцев и выдающихся спортсменов. <br>
  <br>
  Всего за годы войны на базе ОМСБОНа было подготовлено и заброшено в тыл противника 
  около 220 диверсионно-разведывательных отрядов и групп, общей численностью более 
  7500 бойцов и командиров. Из них в ходе операций погибло около 1000, причем 
  далеко не всегда эти жертвы были оправданны. К примеру, командарм генерал Ф. 
  Голиков, когда в его армию прибыли три диверсионно-разведывательных отряда, 
  вместо переправки в тыл противника решил использовать их как пехотные подразделения 
  в ходе наступления. В результате эти отряды и группы вообще перестали существовать. 
  <br>
  <br>
  Следует отметить, что многие из подразделений спецназа - армейского и НКВД - 
  по приказу своего командования принимали участие в так называемой "рельсовой 
  войне". Она сводилась к подрыву отдельных рельсов на железнодорожных путях в 
  немецком тылу. (Этим занимались преимущественно партизанские отряды, но привлекались 
  и диверсанты.) В результате к лету 1943 года на оккупированной территории СССР 
  каждый месяц подрывались 200-300 тыс. рельсов - 2-3% их общего количества. Однако 
  восстановить подорванный 200-граммовой тротиловой шашкой участок рельсового 
  пути для немцев не было большой проблемой. А ведь именно на это тратили время 
  диверсанты вместо подрыва мостов и эшелонов. <br>
  <br>
  Но даже такие, сравнительно непрофессиональные, удары заставили противника задействовать 
  в 1944 году против диверсантов и партизан 140 армейских и полицейских батальонов. 
  <br>
  <br>
  Интересно, что в числе руководителей спецназа было немало евреев: Эйтигон, Серебрянский, 
  Каминский, Маклярский, среди выдающихся диверсантов - Гудимович, Меведев, Колесников, 
  Мирковский и другие. <br>
  <br>
  Деятельность диверсионных подразделений весьма усложнилась после выхода советских 
  войск на территорию Зарубежья. Партизаны остались в СССР, а население Польши, 
  Румынии, Венгрии, Словакии и Германии относилось к советским диверсантам враждебно, 
  и на его поддержку рассчитывать не приходилось. <br>
  <br>
  К этому добавились трудности заброски сухопутным и водным способами, основным 
  стало парашютное десантирование. Однако низкий уровень подготовки пилотов, неправильный 
  выбор площадок для выброса часто приводили к гибели целых групп. К примеру, 
  в 1944 году диверсионная группа "Сталь" была выброшена на германский аэродром 
  и расстреляна в воздухе. Группа "Рул" летом 1944 года десантировалась прямо 
  в расположение немецкого пехотного полка и в полном составе попала в плен. А 
  группа "Одессит" осенью 1944 года угодила в большое озеро, и все ее бойцы утонули. 
  <br>
  <br>
  Тем не менее, несмотря на отсутствие специальных диверсионных формирований в 
  начале войны, поспешность при их организации, слабую связь, нехватку вооружения 
  и боеприпасов и серьезные ошибки в оперативном использовании, советские диверсионно-разведывательные 
  группы и отряды наносили германской армии и ее союзникам вполне чувствительные 
  удары, дезорганизуя снабжение, нарушая коммуникации и функционирование тыловых 
  служб. <br>
