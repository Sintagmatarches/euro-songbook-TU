Известна также как &quot;Казаки в Берлине&quot;. Написана в день победы. Цезарь 
Солодарь в качестве военного корреспондена присутствовал при подписании капитуляции 
8 мая под Берлином. 9 мая рано утром в Берлине он увидел сцену - едут казаки, 
стоит регулировщица, улыбка... В тот же день Солодарь в военно-транспортном самолете 
по пути в Москву написал этот текст и, прилетев, отдал Дмитрию и Даниилу Покрассам. Был полдень. Вечером композиторы позвонили ему и сказали, что музыка готова, и 
он может приехать посмотреть, но домой придется возвращаться пешком, так как метро 
скоро закрывается. Солодарь приехал - песня была готова.<br>
