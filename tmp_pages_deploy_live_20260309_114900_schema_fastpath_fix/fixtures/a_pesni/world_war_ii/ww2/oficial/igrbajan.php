  в передаче &quot;Сеня Ястребков&quot;. <br>
  <br>
  Также это одна из первых песен, записанных на пластинку сразу после возобновления 
  в 1942 году работы московского Дома звукозаписи и Апрелевского завода грампластинок 
  (они не работали с октября 1941-го). Запись началась с коллективов, входящих в 
  Центральный объединенный ансамбль Военно-Морского флота СССР п/у Вано Мурадели, 
  поэтому много песен оказалось на флотскую тематику. Ансамбль записывался с 31 
  августа и в течение сентября 1942. Были записаны &quot;Месть балтийцев&quot; 
  и &quot;Песня морской пехоты&quot; М. Юдина и Н. Браун, <a href="igrbajan.php">&quot;Играй, 
  мой баян&quot;</a> (&quot;Застава дорогая&quot;) В. Соловьева-Седого, Л. Давидович 
  и Г. Ромма, <a href="beskozyrka.htm">&quot;Бескозырка&quot;</a> И. Жака и Н. 
