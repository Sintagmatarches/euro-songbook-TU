  <br>
  1943<br>
  <br>
  <em>Русские советские песни (1917-1977). Сост. Н. Крюков и Я. Шведов. 
  М., «Худож. лит.», 1977.</em><br>
 <br>
  <br>
  Это авторский текст Симонова, который в реальности в таком виде в годы войны 
  не исполнялся (разве что, в кругу близких друзей), так как по требованию цензуры 
  Симонов сделал замены при публикации стихотворения, и именно измененный текст 
  стал песней. Стихотворение написано по пути с одного фронта на другой - из освобожденного 
  Краснодара в еще не освобожденный Ростов-на-Дону. Обычно песня называется &quot;Песня 
  военных корреспондентов&quot;. У Симонова стихотворение озаглавлено &quot;Корреспондентская 
  застольная&quot;. Эту песенку военные корреспонденты - друзья Симонова - пели 
  на мотив <a href="../../dvor/murka.php">&quot;Мурки&quot;</a>, пока Матвей Блантер 
  в том же 1943 году не написал для нее собственную мелодию. <br>
  <br>
  В измененном тексте <em> &quot;От ветров и водки Хрипли наши глотки&quot;</em> 
  стало <em>&quot;От ветров и стужи Петь мы стали хуже</em>&quot;, <em>&quot;Репортер 
  погибнет - не беда&quot;</em> - <em>&quot;Но мы не терялись никогда&quot;</em>, 
  а также был убран куплет про мертвых репортеров. На пластинку песня записана 
  Леонидом Утесовым 3 февраля 1945 года. Первоначальный авторский текст был восстановлен 
  спустя 20 лет, кроме куплета про мертвых репортеров, который в песню все равно 
  не попал. Дело было так. В 1963 году Симонов подарил Утесову свой сборник &quot;Стихи 
  и поэмы&quot;, исправив от руки опубликованный текст песни и сопроводив надписью: 
  &quot;Дорогого Леонида Осиповича Утесова прошу петь только так - на мою голову, 
  а если ее одной мало, то еще и на свою! Ваш Константин Симонов. 25/V 1963 года&quot;. 
  Оркестр Утесова был в отпуске, но Утесов не хотел ждать и через несколько дней 
  исполнил песню с авторским текстом в передаче &quot;С добрым утром&quot; с инструментальным 
  трио. Запись пошла на пластинки, но более известна первая фонограмма. <br>
  <br>
  <br>
  <strong>Из беседы Матвея Блантера с журналистом Глебом Скороходовым в конце 1960-х гг.:</strong><br>
  <br>
  <em>&quot;Вот вы восхищаетесь Утесовым, а у него пошлость проступала постоянно. 
  Ну и что же, что вы не согласны, мне и не нужно вашего согласия, я говорю то, 
  что думаю. Своих песен Утесову я никогда не давал. Это он выпросил у меня «Утро 
  и вечер» и «Девушку» в тридцать седьмом году, когда меня не пели. И «Песню корреспондентов» 
  он не пел первым. Я написал ее к пьесе Симонова «Жди меня» в постановке Николая 
  Михайловича Горчакова – он молодым утонул в Сочи, у самого берега. А «Корреспондентов» 
  в его спектакле спел Слава Плятт, без голоса, но очень строго и артистично. 
  Как песня попала к Утесову, не знаю. Между прочим, когда он начинал в мюзик-холле, 
  то песен не пел. Исполнял куплеты, танцевал, ходил на голове и делал это талантливо. 
  Потом вообразил себя певцом – ничего хорошего не получилось&quot;. <br>
  <br>
  (Скороходов Г. А. Тайны граммофона: Все неизвестное о пластинках и звездах грамзаписи. 
  - М.: Изд-во Эксмо, Изд-во Алгоритм, 2004, с. 77).</em><br>
  <br>
  <br>
  <strong>Из воспоминаний Иосифа Кобзона:</strong><br>
  <br>
  <em>&quot;Вспоминаю, как меня на концерте отвёл в сторону поэт Константин Симонов. 
  (Я исполнял песню Матвея Блантера на его стихи &quot;Песенка военных корреспондентов&quot;.) 
  Отвёл и говорит: &quot;Иосиф, кто Вас учил петь неправильно мои стихи?&quot;<br>
  <br>
  - Да что Вы, Константин Михайлович! Как неправильно? Я всё - по клавиру...<br>
  <br>
  - Нет. У меня не так. То, что Вы поёте (Симонов сильно картавил), придумали 
  перестраховщики. Я так не писал. Вот Вы поёте: &quot;Без глотка, товарищ, песню 
  не заваришь. Так давай по маленькой хлебнём...&quot; А у меня в стихах: &quot;Без 
  ста грамм, товарищ, песню не заваришь...&quot; Или Вы поёте: &quot;От ветров 
  и стужи петь мы стали хуже...&quot;<br>
  <br>
  Я говорю: &quot;Ну?!&quot;<br>
  <br>
  - А у меня, - говорит Симонов, - &quot;От ветров и водки хрипли наши глотки&quot;. 
  Так что, пожалуйста, если Вы хотите петь стихи Симонова, пойте мои стихи, а 
  не то, что Вам эта конъюнктурная редактура направит!<br>
  <br>
  С тех пор я стал петь, как сказал поэт. Поэтому моя &quot;Песенка военных корреспондентов&quot; так отличается от других её исполнителей&quot;.<br>
  <br>
  (&quot;Как перед Богом&quot;, М., &quot;Известия&quot;, 2006). <br>
  <br>
  </em><br>
