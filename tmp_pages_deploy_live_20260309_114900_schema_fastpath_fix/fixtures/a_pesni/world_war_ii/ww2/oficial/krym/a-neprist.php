	  <strong>Г. В. Короткова, Крымское республиканское учреждение «Керченский историко-культурный 
  заповедник»</strong><br>
  <br>
  <span class="header"><strong>НЕПРОСТАЯ ИСТОРИЯ ПАРТИЗАНСКОГО ОТРЯДА ИМЕНИ СТАЛИНА. 
  СТАРОКАРАНТИНСКИЕ КАМЕНОЛОМНИ (1943–1944): К ВОПРОСУ ОБ ОРГАНИЗАЦИИ, БОЕВОЙ 
  АКТИВНОСТИ, ВНУТРЕННИХ ПРОБЛЕМАХ</strong><br></span>
  <br>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=104&par=74&l=&art=596" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, № 19, 2007</em></a><em>.</em><br>
  <br>
  <br>
  Партизанское движение в Керчи и на Керченском полуострове имело свои традиции 
  и особенности, связанные с физико-географическими условиями местности: Керченский 
  полуостров представляет собой слегка волнистую равнину с отсутствием лесных 
  массивов. Единственными базами для возможного укрытия отрядов являлись только 
  каменоломни — подземные выработки камня-ракушечника. Партизанское движение в 
  каменоломнях начало формироваться еще в годы Гражданской войны. В 1919 году 
  в Аджимушкайских и Старокарантинских каменоломнях города Керчи действовали два 
  партизанских отряда. Уже тогда выявились все недостатки базирования партизан 
  в каменоломнях: отсутствие оперативного простора для ведения боевых действий 
  и поддержки извне ввиду полного блокирования противником района подземных выработок.<br>
  <br>
  В годы Великой Отечественной войны на оккупированной территории города Керчи 
  (ноябрь-декабрь 1941, май 1942 — апрель 1944 года) партизанское движение формировалось 
  в два этапа, определявшихся временными рамками оккупации города — с ноября по 
  декабрь 1941 года и с сентября 1943 по апрель 1944 года. На первом этапе движение 
  носило организованный характер, на втором — развивалось стихийно.<br>
  <br>
  Трагический опыт керченских отрядов, разгромленных белогвардейцами в 1919 году, 
  не был учтен в 1941-м, когда перед партийными органами встал вопрос о заблаговременной 
  подготовке партизанской войны в тылу врага в случае оккупации Крыма.<br>
  <br>
  В соответствии с решением ОК ВКП(б) от 4 июля 1941 года в Керчи были созданы 
  два партизанских отряда — в Аджимушкайских и Старокарантинских каменоломнях. 
  Итоги деятельности этих отрядов подведены в отчетах командования, и хотя некоторые 
  приведенные в них цифры потерь противника сегодня вызывают сомнения, тем не 
  менее сама деятельность партизан в достаточной мере изучена и освещена [1].<br>
  <br>
  Гораздо сложнее оценить деятельность трех партизанских отрядов, стихийно возникших 
  и действовавших в Аджимушкайских (Дедушевых и Центральных) и Старокарантинских 
  каменоломнях в период второй оккупации Керчи [2]. Следует отметить слабость 
  документальной базы: ни один из отрядов не вел полной учетной документации и 
  не имел связи с Центральным штабом партизанского движения в Крыму. Единственными 
  источниками изучения боевой деятельности партизан (а также условий их быта, 
  во многом предопределивших и боевую активность) являются отчеты командования 
  отрядов, составленные уже после освобождения города, в связи с проводившимися 
  органами контрразведки проверками деятельности отрядов на оккупированной территории 
  и благонадежности их участников. Отдельные документы (а также воспоминания партизан) 
  появились в 60-е годы, когда в рамках постановления ЦК КПУ «О недостатках в 
  учете участников подпольно-патриотического и партизанского движения в период 
  Великой Отечественной войны…» 28 ноября 1961 года при областном, городских и 
  районных комитетах ВКП(б) создавались партийные комиссии, которые провели дополнительные 
  проверки деятельности некоторых партизанских отрядов. Указанный ряд документов 
  находится на хранении в Государственном архиве в Автономной Республике Крым 
  (ГААРК). Второй группой документов являются воспоминания отдельных партизан, 
  собранные и обработанные в разные годы (начиная с 60-х годов) сотрудниками Керченского 
  историко-археологического музея (с 1988 года — историко-культурного заповедника), 
  которые хранятся в его архиве.<br>
  <br>
  Учитывая условия базирования керченских партизан, крайне ограничивавшие возможности 
  борьбы, проанализируем их деятельность не только в отношении того, какие потери 
  нес противник, но и точки зрения моральной готовности к борьбе с врагом. Именно 
  на этом мы акцентируем внимание, рассматривая деятельность отряда имени Сталина 
  в Старокарантинских каменоломнях с октября 1943 по апрель 1944 года.<br>
  <br>
  Как уже отмечалось, в период второй оккупации Керчи партизанские отряды возникли 
  стихийно. Толчком к их созданию в немалой степени послужило успешное продвижение 
  Красной Армии на южном направлении. То, что Крымский полуостров в ближайшее 
  время станет ареной боев, ни у кого сомнения не вызывало. И естественно искреннее 
  желание мужчин, в силу разных причин остававшихся на оккупированной территории, 
  внести свой вклад в дело освобождения полуострова. С осени 1943 года массовый 
  приток населения в партизанские отряды в Крыму отмечался повсеместно.<br>
  <br>
  Летом 1943 года в поселках Аджимушкай и Старый Карантин из числа местных жителей 
  и освобожденных при их участии из керченских лагерей (или с их помощью избежавших 
  пленения) советских военнослужащих (некоторые из них до войны проживали в Керчи) 
  были созданы инициативные группы — ядро будущих отрядов. Они начали подготовку 
  партизанских баз, создавая в каменоломнях запасы оружия и продовольствия. Параллельно 
  среди населения проводилась агитационная работа, которая, как правило, сводилась 
  к привлечению единомышленников для борьбы с врагом, выпуску и распространению 
  листовок и сводок Совинформбюро.<br>
  <br>
  Дату ухода партизан на места базирования — октябрь 1943 года — определила предпринятая 
  немецким командованием насильственная эвакуация населения в другие районы Крыма. 
  Тогда в каменоломнях укрылись и многие семьи керчан, не пожелавших покидать 
  родные места в преддверии скорого освобождения.<br>
  <br>
  Прибывающее в каменоломни население, среди которого было немало мужчин, пополнила 
  численность партизан. Однако в отряды так попадали и случайные, непроверенные 
  люди. Присутствие в расположении отрядов большого количества женщин и детей 
  возложило на партизан ответственность за их судьбы, потребовало постоянного 
  разрешения проблем продовольствия и водообеспечения, подрывало дисциплину и 
  снижало моральную готовность имеющих семьи партизан к выходам на боевые операции 
  [3]. Наиболее остро эти проблемы проявились в Старокарантинском отряде в 1943 
  году, где не только привели к расколу морального и боевого единства партизан, 
  снижению, а затем и потере боевой активности, но в конечном итоге вылились в 
  акцию самосуда и расправы с некоторыми недавними боевыми соратниками. Трагично 
  сложилась и судьба населения при отряде. Многие мирные жители, наравне с партизанами 
  разделившие все трудности быта в каменоломнях, пережившие голод и жажду, впоследствии 
  были расстреляны немцами и румынами как партизаны.<br>
  <br>
  На наш взгляд, деятельность именно Старокарантинского партизанского отряда, 
  который, несмотря на почти равные условия базирования и материального обеспечения 
  со всеми тремя керченскими отрядами, единственным понес большие потери в личном 
  составе<sup>1</sup> (два других отряда не имели потерь), заслуживает большего 
  внимания. Его боевая деятельность (правда, только на определенном этапе) была 
  результативнее, хотя бoльшие возможности в этом плане имели два других отряда, 
  в составе которых насчитывалось гораздо больше советских военнослужащих с опытом 
  ведения боевых действий.<br>
  <br>
  В документах и в имеющейся на сегодняшний день литературе официально приняты 
  следующие данные о нанесенных противнику потерях: сожжено грузовых машин с боеприпасами, 
  горючим, ацетиленовыми баллонами и радиостанцией — 32, легковых — 4, разбито 
  5 орудий, уничтожено свыше 600 солдат и офицеров противника [4]. Встречается 
  и другая цифра оценки потерь живой силы противника — 670 человек [5]. Возможно, 
  более достоверны те цифры, которые приведены (надо полагать, со слов самих партизан, 
  перешедших в конце февраля линию фронта) в донесении, направленном 29 февраля 
  1944 года управлением контрразведки «Смерш» ОПА начальнику штаба партизанского 
  движения Крыма Булатову:<em> «…партизаны подожгли около 20 а/машин, в т. ч. 
  несколько легковых, 7 тракторов с орудиями, взорвали и частично захватили много 
  боеприпасов, уничтожили до 300 солдат и офицеров».</em> Правда, потери противника 
  приведены на «время Эльтигенской операции» [6], то есть с 1 ноября по 6 декабря 
  1943 года, когда десантники удерживали занятый плацдарм.<br>
  <br>
  Для сравнения<sup>2</sup> приведем официальную оценку потерь, нанесенных противнику 
  двумя другими отрядами — имени Сталина и «Красный Сталинград» — общей численностью 
  76 человек: уничтожено свыше 200 солдат и офицеров противника, выведены из строя 
  1 артиллерийская батарея, 4 зенитные точки, 2 прожектора, сожжены 2 тягача, 
  4 легковых и несколько грузовых автомашин, 2 мотоцикла, захвачены продсклад 
  и много различного оружия<sup>3</sup>.<br>
  <br>
  <hr>
  <br>
  <sup><em>1</em></sup><em> Погиб 31 партизан и столько же пропали без вести.<br>
  <sup>2</sup> Эти отряды действовали только с октября по 11 ноября 1943 г., но 
  сравнение допустимо, если исходить из того, что Старокарантинский отряд наиболее 
  активно действовал на протяжении ноября-декабря 1943 г.<br>
  <sup>3</sup> Эти цифры приведены из отчетов командиров данных отрядов [9].<br>
  По мнению автора, при анализе деятельности всех керченских партизан имеет место 
  явно завышенная оценка потерь противника [10]. В ГААРК приведена и другая, на 
  наш взгляд, более достоверная оценка деятельности отрядов Аджимушкайских каменоломен. 
  Отряды были слабо оформлены организационно, в начале своего существования не 
  предпринимали активных боевых действий, ограничиваясь сбором оружия и заготовкой 
  продовольствия. Как боевые единицы, проявили себя только с 3 по 11 ноября 1943 
  г. Так, отряд «Красный Сталинград» совершал небольшие вылазки, обстреливая проходящие 
  мимо немецкие машины и добывая продукты питания; были захвачены две легковые 
  машины, одна из которых уничтожена, вторая вместе со штабными документами передана 
  советскому командованию. Возле каменоломен убиты жандарм и немец, двое немцев 
  взяты в плен и переданы советскому командованию. Партизаны отряда им. Сталина 
  также крупных боевых вылазок не совершали; после высадки советского десанта 
  вывели из строя 4 зенитные пушки, захватили 2 зенитных пулемета, 4 машины с 
  боеприпасами, небольшой конный обоз, небольшой продсклад, уничтожили и захватили 
  некоторое количество живой силы врага [11].</em><br>
  <br>
  <hr>
  <br>
  Интенсивность боевой деятельности партизан Старокарантинских каменоломен на 
  протяжении всех шести месяцев существования отряда менялась. Попробуем проследить 
  состояние его боеспособности и активности в разные периоды, опираясь на те немногочисленные 
  документы, которые имеются в нашем распоряжении. Анализ боеспособности и активности 
  отряда предполагает оценку его морально-психологического настроя, во многом 
  определявшегося составом участников, а также возникших в отряде упомянутых выше 
  внутренних противоречий.<br>
  <br>
  Организатором отряда являлся К. К. Мухлынин, житель поселка Камыш-Бурун, до 
  войны работавший электриком на аглофабрике. С началом войны он был призван в 
  ряды РККА<sup>4</sup> и, по некоторым данным, участвовал в прикрытии отхода 
  основных сил Крым-фронта на Тамань, не сумел переправиться и остался в поселке, 
  где проживала его семья [7]. По другим данным, он вернулся домой в 1943 году, 
  совершив побег из плена, и в числе других мужчин поселка был привлечен немцами 
  на работу в депо на Камыш-Бурунской железнодорожной ветке [8].<br>
  <br>
  В поселке Мухлынин собрал группу патриотически настроенной молодежи в составе 
  около 10 человек, которая стала ядром будущего отряда. В нее вошли В. Панкратов, 
  Н. Гаврилов, И. Чепелев, А. Филатова и другие. Группа располагала пишущей машинкой 
  и радиоприемником, которые хранились на квартире у А. Филатовой. Она же печатала 
  на машинке листовки и сводки Совинформбюро, которые распространялись в поселке.<br>
  <br>
  Постепенно зрела мысль о создании партизанского отряда. Разместить его решено 
  было на месте базирования партизан в 1941 году. Начались заготовки продовольствия 
  и оружия, которые в ночное время переправляли в каменоломни. Велись работы по 
  расчистке тупиков для размещения продовольственного склада, запасов воды, оборудовались 
  казармы, контрольные посты, баррикады. Последние строились на удалении от входов 
  и представляли собой сложенные из бута заградительные стены с узкими проходами. 
  В стены в 10-литровых бутылках закладывали фугасы (оставшиеся от партизан 41-го 
  года), которые посредством протянутых проводов присоединяли к аккумулятору. 
  В случае появления противника замыканием цепи производился взрыв. Предполагалось 
  численность отряда довести до 30 человек (только мужчин), в самом же поселке 
  оставить связных и разведчиков.<br>
  <br>
  Известно, что оружия и боеприпасов в достаточном количестве заготовить не удалось. 
  Около 10 винтовок с небольшим количеством патронов нашли в самих каменоломнях. 
  Еще столько же винтовок, патроны к ним и некоторое количество гранат выкрали 
  у немцев. Из-за неумелого обращения с гранатами партизаны понесли первые потери. 
  При опробовании их действия получил ранение и скончался боец отряда Косенко. 
  Это заставило партизан организовать санитарную часть. В отряд были привлечены 
  две девушки, работавшие в Старом Карантине в немецкой санчасти. Они принесли 
  с собой большое количество медикаментов<sup>5</sup>.<br>
  <br>
  <hr>
  <br>
  <sup><em>4</em></sup><em> Подробными сведениями о прохождении им воинской службы 
  мы не располагаем.<br>
  <sup>5</sup> По другим данным, медикаменты и хирургические инструменты были 
  захвачены при нападении на немецкую санитарную часть, а работавшие в ней русские 
  девушки добровольно ушли в отряд [12].</em><br>
  <br>
  <hr>
  <br>
  Переход первых партизан на место базирования был ускорен диверсией на железной 
  дороге. Перегоняя вручную платформу на другой путь, чтобы освободить колею груженому 
  составу, железнодорожники под колеса платформы подложили непригодные железнодорожные 
  башмаки. Дорога шла под уклон, и покатившаяся вниз платформа (также груженая) 
  врезалась в движущийся состав. Заподозренных железнодорожников арестовали и 
  содержали в течение двух недель в керченской тюрьме. По воспоминаниям жены К. 
  Мухлынина, Н. Я. Марченко, от расправы их спасло то обстоятельство, что они 
  предусмотрительно спрятали исправные башмаки. Ее саму с ребенком продержали 
  сутки в комендатуре и, направляя на ребенка оружие, требовали дать показания, 
  что муж является партизаном. После выхода из тюрьмы указанная группа лиц во 
  главе с Мухлыниным ушла в каменоломни. О серьезной настроенности на борьбу свидетельствует 
  тот факт, что К. Мухлынин в целях безопасности отправил семью из города [13]. 
  Другой партизан, В. Панкратов, отклонил просьбу матери забрать ее в каменоломни 
  с двумя дочерьми, объяснив, что «у него отряд» [14].<br>
  <br>
  Первые 36 человек прибыли в отряд 3–5 октября 1943 года. Приток людей увеличился 
  в связи с начавшейся эвакуацией населения из Орджоникидзевского района. С 15 
  по 31 октября партизанами стали еще 30 человек. В ноябре-декабре рост численности 
  отряда продолжался. 10 человек, также укрывавшихся от эвакуации, прибыли из 
  Оливенских каменоломен<sup>6</sup>. Здесь небольшой группой руководил, по некоторым 
  данным, красноармеец Семен<sup>7</sup>. В наличии имелось оружие, подобранное 
  с мест боев 1942 года (в том числе одно ПТР), из которого обстреливали автотранспорт 
  противника. Насколько продолжительна и эффективна была их деятельность, неизвестно. 
  Член этой группы А. Эмин вспоминает, что первое столкновение с противником, 
  во время которого он был ранен, оказалось неудачным. Через три недели удалось 
  установить связь с отрядом К. Мухлынина, куда их сопроводили прибывшие разведчики 
  [15].<br>
  <br>
  <hr>
  <br>
  <sup><em>6</em></sup><em> Оливенские каменоломни находятся недалеко от Старокарантинских, 
  свое название получили от н. п. Оливенка, ныне исключенного из учетных данных.<br>
  <sup>7 </sup>Им мог являться Семен Фраянс, 1915 г. р., прибывший в отряд 27 
  ноября. Датой прибытия в отряд других участников этой группы, включенных в списки 
  отряда, указано 28 ноября. Местным жителем, судя по всему, не являлся.<br>
  </em><br>
  <hr>
  <br>
  Последние даты прибытия в отряд — 25 декабря (прибыли 3 человека) и 28-го (1 
  человек). Численность отряда по спискам, утвержденным в 1962 году, составила 
  102 человека (из них 11 женщин). По спискам же, представленным в 1944 году в 
  КШПД для утверждения состава отряда, — 89 человек (из них 10 женщин) [16].<br>
  <br>
  Состав отряда был сравнительно «молодым»: молодежи 1923–1928 годов рождения 
  — 65 человек, остальные 1903–1922 годов рождения, из них военнослужащих — 7 
  человек. Возможно, количество военнослужащих было больше указанного, поскольку 
  мужчины 1903–1922 годов рождения призывались еще в 1941 году, а молодежь 1923–1924 
  годов рождения подлежала призыву в 1942 году, когда город был освобожден. Возникает 
  вопрос: почему на оккупированной территории остались мужчины призывного возраста? 
  Не укрывались ли они от мобилизации, а в дальнейшем, когда шли завершающие бои 
  по освобождению Таманского полуострова и линия фронта вплотную приблизилась 
  к Керченскому проливу, не использовали ли пребывание в партизанском отряде как 
  своеобразную реабилитацию своего нахождения на оккупированной территории? Последнее 
  относится и к военнослужащим, по разным причинам оставшимся на керченском берегу, 
  и к военнопленным, этапированным в Керчь, которые понимали, что после освобождения 
  города их ждут спецпроверки.<br>
  <br>
  Можно предположить, что многие из подлежащих мобилизации керчан имели бронь 
  от производства. Так, в составе отряда числятся семь железнодорожников 1905–1920 
  годов рождения, которых могли не призвать ввиду важности обеспечения железнодорожных 
  перевозок. Кроме того, после освобождения города встал вопрос о восстановлении 
  предприятий (в первую очередь черной металлургии), а также о налаживании бесперебойной 
  работы керченских портов и железной дороги. В Керчи наблюдался большой дефицит 
  занятых рабочих мест. В связи с этим интересно письмо, направленное 18 марта 
  1942 года секретарем ОК ВКП(б) В. С. Булатовым в Военный совет Крымфронта, в 
  котором он просит дать повторную отсрочку (первая была в январе того же года) 
  подлежащим мобилизации военнообязанным из-за угрозы срыва выполнения военных 
  заказов керченскими предприятиями [17].<br>
  <br>
  Сведения о призыве некоторых партизан содержатся в их воспоминаниях. Так, Н. 
  Куринев (1924 г. р.) призван в ряды РККА в марте 1942 года. Воевал рядовым, 
  под Сталинградом попал в плен, бежал. До-брался в деревню Ново-Алексеевка Ленинского 
  района, где был выдан татарами и заключен в Семиколодезянский лагерь. Снова 
  бежал, пришел домой, в свой поселок. Узнав об организации отряда, ушел в партизаны 
  [18]. Возможно, в отряде он был не единственным вчерашним школьником, кому довелось 
  побывать в бою, хлебнуть горечь поражения и ужасы плена. Несомненно, такие люди 
  шли в отряд бороться осознанно. По некоторым данным, на фронте под Севастополем 
  попал в плен и потом бежал также старшина 2-й статьи, моряк ЧФ А. Чередниченко 
  [19].<br>
  <br>
  Списки гражданского населения, находившегося при отряде, отсутствуют. Судя по 
  всему, четкого разделения на партизанский и гражданский лагеря (как, к примеру, 
  в партизанском отряде в Багеровских каменоломнях на территории Маяк-Салынского 
  района) не было. Сами же партизаны, говоря о численности отряда, скорее всего, 
  относят сюда и гражданское население. Так, партизан П. Марченко приводит цифру 
  170–180 человек, Н. Гаврилов — 200–230, но при этом не указывают, это только 
  боевой состав или в том числе гражданские лица. Более конкретны цифры Т. Кокошко 
  — до 140 человек, «…в том числе женщины, старики, дети», и А. Эмина — всего 
  180 человек, из них бойцов — 80, а «…остальные были женщины, которых вынуждены 
  были за-брать…». Эти данные представляются нам более реальными. Приблизительно 
  такое же количество называет и С. М. Лазарев<sup>8</sup>, который по поручению 
  партийных и советских органов занимался составлением списков отряда сразу же 
  после освобождения города: «Я… устанавливал этот факт, кто с ними (имеет в виду 
  партизан. — Г. К.) был. Мы насчитали 170 человек…» [20]. Надо полагать, что 
  это общее количество всех находившихся в каменоломнях.<br>
  <br>
  <hr>
  <br>
  <sup><em>8</em></sup><em> С. М. Лазарев в 1941 г. являлся командиром отряда 
  в этих же каменоломнях, в 1943-м сражался в других партизанских отрядах Крыма. 
  Ранен в Зуйских лесах, после излечения, с 12 апреля 1944 г., находился в Керчи.</em><br>
