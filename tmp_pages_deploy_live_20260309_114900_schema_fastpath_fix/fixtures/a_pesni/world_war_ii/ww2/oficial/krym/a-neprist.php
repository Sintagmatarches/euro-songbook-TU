  <br>
  Между тем события в отряде приближались к трагическому завершению. Попробуем 
  их реконструировать.<br>
  <br>
  Угроза голода стала реальной. 5–10 марта в отдаленные сельские районы (где проживали 
  родственники партизан) были посланы две группы с целью добычи продуктов. Одновременно 
  они должны были вы-яснить пути возможного вывода из каменоломен населения и 
  его последующего размещения. Одна из этих групп, в которую вошли П. Марченко, 
  И. Чепелев, А. Савельев, А. Чижов, получила задание добраться до поселка Первомайское 
  Маяк-Салынского района. Вернуться же в каменоломни эти партизаны не смогли: 
  противник оборудовал пулеметные точки и посты, заминировал подступы к каменоломням 
  и входы в них.<br>
  <br>
  Другой состав — Г. Товарчи, К. Яцуна, Ф. Пономаренко и еще один партизан, фамилия 
  которого не упоминается, — был направлен в деревню Ново-Николаевка того же района. 
  И эта группа в каменоломни не вернулась. Известно, что двое партизан отделились 
  и ушли в неизвестном направлении (Пономаренко внесен в списки как дезертир), 
  а Товарчи и Яцун были задержаны в районе Ново-Николаевки. Товарчи удалось бежать, 
  а Яцун в сопровождении румын 23 февраля был доставлен к каменоломням. При попытке 
  укрыться в них он был убит своими: его приняли за инженера Спано, сотрудничавшего 
  с немцами. Со слов Кокошко, обвинять Яцуна в предательстве не было никаких оснований: 
  он прекрасно ориентировался в каменоломнях и мог провести румын к месту расположения 
  отряда незаметно, а между тем вывел их прямо на пост. На теле Яцуна были обнаружены 
  следы пыток — его избивали и жгли раскаленным прутом. Тем не менее Чередниченко 
  внес его в списки предателей [29].<br>
  <br>
  Таким образом, проблему вывода населения из каменоломен решить не удалось.<br>
  <br>
  15 марта, в связи с полным отсутствием продовольствия, Чередниченко снял с себя 
  командование и предложил пробиваться «кто как сможет». Часть партизан во главе 
  с комиссаром Васюниным решилась на переход в Крымские леса, но была обнаружена, 
  приняла бой и погибла. Другие партизаны (а также гражданские) покидали каменоломни 
  мелкими группами и искали укрытия в близлежащих населенных пунктах. Некоторые 
  вместе с семьями продолжали оставаться в каменоломнях, в том числе и отделившаяся 
  группа Рязанова. Он предложил оставшимся партизанам провести совместные вылазки 
  за продуктами. Однако в отряд никто из них не вернулся. Возникли подозрения, 
  что их с целью присвоения добытых продуктов расстреляли «рязановцы». Ходили 
  слухи и о том, что последние собираются выйти на поверхность, сдаться в плен 
  и выдать партизан. Было принято решение их «убрать». Когда 25 марта «рязановцы» 
  пришли в расположение отряда, они были расстреляны как предатели. В их числе 
  братья Бунины, Рязановы, А. Калиевский. Успел скрыться И. Амелин. Вместе с семьей 
  Рязанова, Г. Товарчий и некоторыми другими мирными жителями он вышел на поверхность, 
  где все были расстреляны румынами. После освобождения города их трупы (всего 
  16) в двух могилах в районе каменоломен были обнаружены членами комиссии по 
  расследованию злодеяний, совершенных немецко-фашистскими захватчиками на территории 
  Орджоникидзевского района [30].<br>
  <br>
  Как бывший командир и боевые товарищи вдруг стали для остальных врагами, которых 
  заподозрили в возможном предательстве и, более того, обвинили в расстреле недавних 
  соратников [31]? Какие причины вызвали ситуацию недоверия и подозрительности?<br>
  <br>
  Происшедший конфликт следует рассматривать с учетом, прежде всего, психологического 
  фактора. Люди, спустившиеся в каменоломни, ставили перед собой разные цели. 
  Одни, ядро отряда, были нацелены на борьбу с врагом. Другие рассчитывали укрыться, 
  «отсидеться» и дождаться уже очевидно скорого освобождения. Ведение боевых действий 
  в их первоначальные планы не входило. Нелегко, видимо, было подчиниться и принятому 
  решению о сдаче на склад продуктов, от наличия которых зависела жизнь их близких. 
  Между тем казавшееся близким освобождение затягивалось. Пребывание на протяжении 
  длительного времени в условиях замкнутого пространства негативно влияло на здоровье 
  человека, сказывалось на отношениях между людьми. Ситуация усугублялась наличием 
  противника, трудностями быта, а в дальнейшем — осознанием явной безвыходности 
  положения. Здесь и сказалось отсутствие однородного коллектива, имеющего единые 
  взгляды и цели.<br>
  <br>
  Но и после смены руководства боевая активность отряда не возросла. Каменоломни 
  покидали военно-служащие и сопровождавшие их партизаны, а оставшимся предстояло 
  решить дальнейшую судьбу отряда. Намерение перейти в Крымские леса объясняет 
  попытку вывода из каменоломен населения. Но партизаны, посланные в разведку 
  для установления связи с отрядами Старого Крыма, а также в села для определения 
  населения на проживание там, не возвратились. Продукты питания закончились. 
  В этих условиях Чередниченко сложил с себя полномочия. Таким образом, организационно 
  отряд распался 15 марта, а как боевая единица — еще раньше, в феврале 1944 года. 
  С этого момента активные боевые действия, за исключением вылазок за продуктами, 
  не велись.<br>
  <br>
  После 15 марта в каменоломнях оставались партизаны, имевшие семьи, переход с 
  которыми куда-либо был невозможен. Все они были уничтожены румынами 8 апреля 
  1944 года во время прочеса каменоломен. Среди них — партизаны Спасибенко (отец 
  и два сына), Н. Абраменко, Л. Капустина, Н. Кравцов, С. Поляков, И. Бирчак, 
  трупы которых, а также членов их семей (всего 39 человек) со следами пулевых 
  и осколочных ранений были обнаружены в каменоломнях членами упоминавшейся выше 
  комиссии Орджоникидзевского района [32]. Только 6-ти партизанам, не связанным 
  семьями, в том числе и Чередниченко, удалось укрыться в отдаленных выработках, 
  откуда они и были извлечены при обследовании каменоломен после освобождения. 
  При этом имело место досадное недоразумение. Не имея сведений об освобождении 
  города, 16 апреля партизаны застрелили одного из саперов, участвовавших в разминировании 
  каменоломен, приняв его за румына [33].<br>
  <br>
  Известные впоследствии факты расстрела одних партизан другими, а также обнаруженные 
  в каменоломнях трупы погибших партизан и мирных граждан стали предметом дополнительной 
  проверки деятельности отряда. Инициатором проверки, проведенной в 1962 году 
  внештатным инструктором парткомиссии при Крымском обкоме КПУ Г. Крючковым, стал 
  коммунист А. Я. Колобов, поставивший под сомнение факт существования отряда 
  и проведения им активных боевых действий. Будучи в 1944 году председателем Орджоникидзевского 
  райисполкома, он совместно с С. М. Лазаревым занимался составлением списков 
  партизан и изучением деятельности отряда. С его слов, он прекратил этим заниматься 
  после того, как выяснил, «…что в скале возникло недопонимание — образовалась 
  группа слабых и сильных» [34]. Однако упомянутая ситуация не стала в то время 
  объектом пристального внимания со стороны органов НКГБ, тем более что подобный 
  случай имел место в отряде «Красный Сталинград», где по подозрению в предательстве 
  и сотрудничестве с немцами были расстреляны два партизана из соседнего отряда 
  имени Сталина, прибывшие туда для установления связи. Этот эпизод наряду с другими 
  обстоятельствами, дискредитирующими отряд [35], поставил последний в разряд 
  сначала «сомнительного», а после расследования его деятельности и проведенных 
  спецпроверок в отношении некоторых партизан и самого командира — «лжепартизанского». 
  С партизанским отрядом Старокарантинских каменоломен этого не произошло. Подлежащие 
  призыву уцелевшие партизаны после недолгих спецпроверок были направлены на фронт, 
  в том числе и последний командир Чередниченко.<br>
  <br>
  В 1962 году прояснить до конца ситуацию относительно сложившейся в отряде обстановки 
  и происшедших после 15 марта событий не удалось: очевидцев среди приглашенных 
  на беседу бывших партизан (по поводу рассмотрения заявления А. Колобова) не 
  оказалось. С их слов, в отряде «до 15 марта был идеальный порядок, никаких разногласий 
  не было, за исключением того, что группа Рязанова ушла в другое место» [36]. 
  Сам Чередниченко в 1944 году на фронте пропал без вести.<br>
  <br>
  Так кем считать расстрелянных своими же партизан? Их фамилии, в отличие от партизан 
  отряда имени Сталина Дедушевых каменоломен, расстрелянных в соседнем отряде 
  «по недоразумению», не вошли в 6-й том Книги Памяти Автономной Республики Крым. 
  Не войдут они и в списки погибших партизан 2-го тома Книги Памяти «Город-герой 
  Керчь и Керченский полуостров».<br>
  <br>
  Каждый керченский партизанский отряд имеет свою непростую историю, о которой 
  ранее было не принято говорить. Превозносились (и при этом преувеличивались) 
  только боевые заслуги партизан и желание драться с врагом. Такое желание у большинства 
  действительно было, но в итоге сводилось «на нет» бесперспективностью базирования 
  в каменоломнях, слабой боевой подготовкой и отсутствием достойных командных 
  кадров. В 1941 году это были чужие ошибки и «непродуманные решения». В 1943-м 
  партизаны расплачивались за свои собственные. Можно лишь привести слова бывшего 
  партизана Старокарантинских каменоломен 1943 года А. Эмина: «…люди, которые 
  случайно остались живы, или те, которые погибли, просидев шесть месяцев в каменоломнях, 
  сделали все, что было в их силах. Совершенно неправильно, и не следует говорить 
  о том, что этого партизанского отряда не существовало» [37].<br>
