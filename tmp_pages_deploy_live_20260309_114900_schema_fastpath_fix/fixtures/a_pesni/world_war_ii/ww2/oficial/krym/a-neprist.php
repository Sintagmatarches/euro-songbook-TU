  <br>
  К концу октября организационные мероприятия по формированию отряда были завершены. 
  Партизаны приступили к боевой деятельности. К этому времени население Орджоникидзевского 
  района было эвакуировано. Партизанам предстояло действовать на обезлюдевшей 
  территории. Они были лишены поддержки со стороны населения, которое сами вынуждены 
  были взять под свою опеку. Поэтому вылазки с целью пополнения запасов продовольствия 
  приобретали жизненно важное значение.<br>
  <br>
  С момента организации отряда была предпринята попытка установить связь с регулярными 
  советскими частями. 8 октября группа партизан из пяти человек под видом ловли 
  бычков должна была выйти на ялике в море и переправиться на Таманский берег. 
  Эта попытка оказалась неудачной. Партизаны были задержаны немецким сторожевым 
  катером. Трое погибли, а двое других некоторое время содержались в керченской 
  тюрьме и были расстреляны [23].<br>
  <br>
  К сожалению, в нашем распоряжении нет достоверных источников для оценки боевых 
  действий отряда и понесенных противником потерь. Дневник боевых действий, который, 
  по воспоминаниям партизан, вел комиссар Д. Васюнин, не сохранился. Хронология 
  боевых действий приведена в брошюре «Моряки-черноморцы — партизаны», изданной 
  в 1944 году Политуправлением ЧФ [24].<br>
  <br>
  Направленность боевых действий партизан, которые заключались в нападении на 
  гужевой и автотранспорты противника, предопределило само расположение каменоломен 
  — в непосредственной близости от старого Феодосийского шоссе и дорог, ведущих 
  в сторону Камыш-Буруна и Эльтигена. Первые боевые вылазки провело отделение 
  разведки, удаляясь от каменоломен и по возможности уничтожая одиночных солдат 
  противника. Разведчиками было совершено и первое нападение на немецкую легковую 
  автомашину: находившиеся в ней два офицера и шофер были убиты, партизанам достались 
  автомат и пистолет системы «Вальтер» [25]. Как вспоминал И. Чепелев, другим 
  партизанам тоже не терпелось попробовать свои силы, а главное — добыть в бою 
  оружие. К этому времени отряд имел в наличии только до 30 винтовок с ограниченным 
  запасом патронов, 2–3 автомата и до 100 штук гранат различного производства.<br>
  <br>
  28 октября разведка доложила о том, что в районе каменоломен на ночлег остановилась 
  колонна немецких автомашин. Военный совет принял решение о проведении первой 
  боевой вылазки всем составом отряда. Партизаны находились в приподнятом настроении, 
  от расположения отряда до выхода шли, распевая песню: <em>«…партизанские отряды 
  занимали города…».</em> Разведчики, ждавшие у выхода, указали исходный рубеж 
  атаки. Партизаны подползли к автоколонне и замерли в ожидании сигнала — выстрела 
  командира. Вот как описал первый бой отряда И. Чепелев: <em>«Вслед за одиночным 
  выстрелом последовали дружные залпы партизан по автомашинам. Кто имел гранаты 
  — бросали, они взрывались в гуще автомашин. Четыре машины загорелись, немцы 
  стали в панике выскакивать. Загоревшиеся автомашины хорошо освещали немцев, 
  которых партизаны расстреливали с криками «Ура!».</em><br>
  <br>
  Но горящие автомашины хорошо освещали и нас. Немцы понемногу стали организовывать 
  ответный огонь. Укрывшись за колесами автомашин, они вели из автоматов все более 
  усиливающийся огонь. По цепи быстро пронеслась весть, что ранен Евгений Паневский. 
  Его понесли к входу в шахту. Другие, видимо, посчитали, что отряд отходит, и 
  тоже стали заходить в шахту. Немного позже весь отряд под все возрастающим огнем 
  немцев вынужден был отступить, не взяв никаких трофеев. Наоборот, отряд отошел 
  почти обезоруженный. Тот считанный запас боеприпасов, который был, расстреляли, 
  гранаты разбросали, и остались почти ни с чем. Наступили тяжелые дни. Настроение 
  было подавленное. Каждую минуту мы ждали, что немец пойдет в шахту. А чем его 
  бить? Патроны расстреляны, гранат нет. На постах охрана была усилена вдвое [26].<br>
  <br>
  Уже после первой вылазки среди партизан возникли разногласия. Некоторые (Рязанов, 
  Чинов) стали упрекать командование в неправильных действиях и высказались за 
  отказ от боевых операций и «сохранение» сил отряда до прихода регулярных советских 
  войск. Военный совет принял решение о продолжении активных боевых действий. 
  Чтобы поднять боевой дух партизан после первого неудачного боя, И. Чепелев со 
  своим отделением высказался за проведение новой вылазки. В боевом выходе также 
  участвовали командир и комиссар, отделение разведки. Засаду устроили днем на 
  дороге Керчь — Феодосия. Объектом нападения партизан стала одиночная подвода. 
  Уничтожены 2 немца, трофеи: 2 винтовки, 2 парабеллума, 4 ящика патронов и 2 
  — гранат.<br>
  <br>
  Это был первый успех. Партизаны вернулись в отряд «героями». Не участвовавшие 
  в вылазке партизаны тоже рвались в бой. По их настоянию командование разрешило 
  в этот же день выход на Керчь — Камыш-Бурунскую дорогу. Партизаны подбили из 
  засады мотоцикл с водителем и офицером, а немного позднее на дороге появилась 
  колонна из 6–7 крытых грузовых автомашин. Первая машина была подбита. Образовалась 
  пробка, по выпрыгивающим из машин немцам открыли огонь. Трофеи: до 50 винтовок 
  и 5 автоматов, 30 ящиков патронов и 20 ящиков гранат. С этого дня партизаны 
  перешли на немецкое вооружение, боеприпасов было достаточно [27].<br>
  <br>
  Наиболее активно отряд действовал в ноябре 1943 года, после высадки советского 
  десанта в район Эльтигена, когда немцы перебрасывали подкрепление, технику и 
  боеприпасы к захваченному десантниками плацдарму. Потери партизан в этом месяце 
  составили 4 человека, в их числе — погибший 3 ноября в бою командир отряда. 
  Его сменил М. Рязанов (по другим данным, некоторое время отрядом командовал 
  В. Панкратов). Смена руководства повлекла за собой снижение боевой активности 
  и внутренний раскол в партизанской среде. Повлияли на это и мероприятия против 
  партизан, которые противник предпринял после ликвидации эльтигенского плацдарма. 
  Можно предположить, что в ноябре каких-либо широкомасштабных акций по отношению 
  к отряду немецкое командование не предпринимало: немецко-румынские части вели 
  ожесточенные бои против высадившихся десантников сразу на двух направлениях 
  — южном (в районе Эльтигена) и востоке и северо-востоке Керченского полуострова.<br>
  <br>
  12 декабря для борьбы с партизанами был направлен румынский батальон. Столкновения 
  с румынами происходили и ранее, с 4 по 8, 10 декабря. Тем не менее полной блокады 
  каменоломен установлено не было. В декабре потери среди партизан не зафиксированы.<br>
  <br>
  9 декабря произошло событие, которое, казалось, должно было существенно повлиять 
  на усиление боевой активности партизан: разведчики доставили в каменоломни группу 
  советских военнослужащих — 14 человек из состава эльтигенского десанта во главе 
  с командиром 335 гв. сп 117 гв. сд полковником П. И. Нестеровым. Сами партизаны 
  отмечали, что с приходом военнослужащих в отряде окрепла дисциплина, были созданы 
  штаб, партийная (из числа прибывших) и комсомольская организации. Нестерову 
  предложили возглавить отряд, но он отказался, мотивируя тем, что ему необходимо 
  пробиваться на соединение с действующими частями, и согласился временно исполнять 
  обязанности начальника штаба. В отряде «эльтигеновцы» пробыли более двух месяцев, 
  но их присутствие не привело к всплеску боевой активности. Как позднее указывал 
  Нестеров в своем объяснении, составленном по факту пребывания в партизанском 
  отряде, провести разработанные им около 10 операций, несмотря на утверждение 
  их советом отряда, он не смог, так как партизаны «не соглашались». Были проведены 
  только две из них, которыми он руководил лично, но успеха они не имели, поскольку 
  партизаны не отходили от каменоломен далее чем на пять километров, а «при первых 
  же выстрелах бежали, бросая даже своих раненых товарищей» [28]. (Последнее явно 
  преувеличено, так как партизаны не оставляли на поле боя и погибших, местом 
  захоронения которых указаны каменоломни). Начальник штаба этого же полка Х. 
  Ф. Ванжа вспоминал, что партизаны совместно с военнослужащими совершали небольшие 
  вылазки. Так, во время одной из них была захвачена автомашина с крупой и повозка 
  с двумя лошадьми<sup>10</sup>.<br>
  <br>
  <hr>
  <br>
  <sup><em>10</em></sup><em> Воспоминания собраны в 1985 г. руководителем поискового 
  клуба «Эльтиген» Т. И. Сафиной (Замуленко).</em><br>
  <br>
  <hr>
  <br>
  Надо полагать, что причина указанной Нестеровым пассивности кроется прежде всего 
  в том, что среди партизан сложились по крайней мере три точки зрения относительно 
  дальнейшей судьбы отряда, руководство которым в это время, скорее всего, принял 
  М. Рязанов (1904 г. р.). Он предложил все усилия направить на спасение населения: 
  к этому моменту все ощутимее стала нехватка продовольствия и воды. Его точку 
  зрения разделяли некоторые мужчины, прибывшие в каменоломни с семьями. Другие 
  партизаны, сгруппировавшиеся вокруг В. Панкратова, были за проведение активных 
  боевых действий. Среди партизан имело место также и стремление к переходу в 
  Старокрымские леса. Для установления связи с партизанами Старого Крыма еще 30 
  ноября была направлена разведка, которая поставленной задачи не выполнила.<br>
  <br>
  Все заметнее становились признаки назревающего конфликта. Его очевидное проявление 
  спровоцировало нарушение группой партизан во главе с В. Панкратовым запрета 
  командира отряда на совершение без его ведома боевых вылазок. В один из январских 
  дней «панкратовцы» напали на запряженную быками румынскую подводу с продовольствием. 
  Ездовой был убит, а подводу с быками они до вечера держали под обстрелом, не 
  подпуская прибывших румын. С наступлением темноты партизаны порубили на части 
  быков и вместе с продуктами перенесли в каменоломни. За нарушение приказа командира 
  Панкратов был арестован с изъятием оружия. Вспыхнул конфликт: одни партизаны 
  вступились за Панкратова, другие поддержали Рязанова. Конфликт мог перерасти 
  в столкновение, но его погасили военные. Участников конфликтующих сторон по 
  очереди вызывали к Нестерову. «Панкратовцы» настаивали на смещении М. Рязанова. 
  В противном случае было решено отделиться, уйти в другие выработки и создать 
  свой отряд. Но достигнут компромиссный вариант: новым командиром отряда стал 
  А. Чередниченко (1916 г. р.), до войны — рыбак. Панкратова оставили на должности 
  заместителя командира.<br>
  <br>
  Группа партизан, поддержавшая М. Рязанова, ушла из расположения отряда вместе 
  с семьями, унеся с собой часть продовольствия. Вместе с тем они продолжали принимать 
  участие в совместных вылазках за продуктами, иногда подобная инициатива исходила 
  лично от них.<br>
  <br>
  В феврале закончились запасы продовольствия. 18 февраля «нестеровцы» приняли 
  решение о прорыве через линию фронта. Помочь им в этом должны были партизаны. 
  Для прорыва создавались смешанные «пятерки». Все участвовавшие в переводе военнослужащих 
  партизаны должны были вернуться в отряд, чего им сделать не удалось: каменоломни 
  были полностью блокированы. По некоторым данным, в состав первой группы вошел 
  В. Панкратов. При возвращении в отряд он был схвачен и позднее расстрелян в 
  поселке Семь Колодезей<sup>11</sup>. В состав других групп вошли партизаны Н. 
  Гаврилов, Н. Куренев, В. Оксанич, Т. Кокошко, В. Саляхов, М. Федоренко. Группа, 
  в которую вошли партизаны А. Филатова, В. Стародонкин, А. Шинковский, П. Ниделков 
  и от военнослужащих Н. Губецкая (Голубецкая), была обнаружена. Линию фронта 
  перешел только Ниделков. Губецкая попала в лагерь, откуда ей помогли бежать 
  местные жители. Трое других были расстреляны в Семи Колодезях. Их тела, а также 
  В. Панкратова были позднее опознаны родственниками. Участвовавшие в переводе 
  последней группы партизаны Кокошко и Федоренко оставались с военнослужащими 
  до конца: ответственность задания усиливалась наличием сейфа со штабными документами 
  335 сп и неврученными наградами.<br>
