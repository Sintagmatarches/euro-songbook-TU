  <br>
  Отряд было решено назвать именем Сталина. Командиром на общем собрании избрали 
  К. Мухлынина, судя по всему пользовавшегося большим авторитетом. Его заместителем 
  стал В. Панкратов (1926 г. р.), работавший до оккупации слесарем на СРЗ. Как 
  вспоминают сами партизаны, несмотря на молодость, избрание на эту должность 
  определило то, что он «…мальчик был смышленый, отлично знал все хода (в каменоломнях. 
  — Г. К.), немецкий язык, был боевой» [21]. Комиссаром стал Д. Васюнин (1914 
  г. р.) по специальности штукатур. Были созданы Военный совет отряда, четыре 
  боевых отделения, которыми командовали М. Федоренко (1923 г. р.), И. Чепелев 
  (1924 г. р.), М. Стороженко и В. Родин (оба 1925 г. р.) и отделение разведки. 
  По одним данным, им командовал А. Чередниченко (1916 г. р.), по другим — В. 
  Панкратов. Почему все отделения не возглавили имевшиеся в отряде военнослужащие, 
  сказать сложно. Некоторые из них прибыли в каменоломни, когда отряд уже организационно 
  оформился. Возможно, выбор пал на местных жителей, так как партизаны их хорошо 
  знали. Учитывался и фактор знания местности, на которой придется воевать.<br>
  <br>
  Были организованы госпиталь, склад, где хранились заготовленные продукты, кухня. 
  Запасы продовольствия пополнялись в результате вылазок за продуктами. Партизаны 
  нападали в основном на одиночно следовавшие румынские подводы. Все добытые продукты 
  подлежали сдаче на склад.<br>
  <br>
  Запасы воды хранились на «водном» складе, в оставшихся от партизан 1941 года 
  брезентовых ваннах (около 30-ти) и опрокидывающихся частях вагонеток. Со слов 
  Т. Кокошко, трубы для подачи воды были подведены к одному из входов каменоломен 
  (так называемый Васильцов выход) еще в 1941 году. Вода бежала под уклоном и 
  собиралась в одном из тупиков, откуда ее носили канистрами на склад. Поступала 
  вода из резервуара, находящегося в районе телецентра. Для ее поступления необходимо 
  было отвернуть кран на резервуаре. Эту работу в ночное время выполнял С. Зубашко, 
  работавший при немцах в системе водоснабжения. Позже возле резервуара румыны 
  устроили засаду и взорвали его. Партизаны, лишившись этого источника воды, «воду 
  раскопали под одним из шурфов, откуда брали до 13 канистр в день» [22]. Возможно, 
  речь здесь идет о так называемых затапливаемых районах, где действительно может 
  стоять вода<sup>9</sup>.<br>
  <br>
  <hr>
  <br>
  <sup><em>9</em></sup><em> Подобные затапливаемые районы имеются в настоящее 
  время в Дедушевых каменоломнях. Следы затопления имеются и в некоторых районах 
  Центральных каменоломен.</em><br>
