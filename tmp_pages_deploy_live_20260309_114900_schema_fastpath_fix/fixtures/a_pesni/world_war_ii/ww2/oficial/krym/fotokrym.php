	<span class="header"> <strong>ФОТОЛЕТОПИСЬ ВОЙНЫ: ПАРТИЗАНСКИЕ БУДНИ</strong><br></span>
  <br>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=104&par=74&l=&art=588" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, № 
  19, 2007</em></a>.<br>
  <br>
  <br>
  Через десять дней после освобождения Крыма от немецко-фашистских оккупантов 
  руководством Крымской АССР было принято решение о создании Комиссии по истории 
  Великой Отечественной войны с целью сбора документов о деятельности подпольных 
  организаций и партизанском движении в Крыму. Комиссия работала три года — до 
  июня 1947 года. За это время были собраны документы начального периода войны 
  (июнь — ноябрь 1941 г.), обороны Севастополя; воспоминания командиров и бойцов; 
  стенограммы бесед с партизанами и подпольщиками; списки подпольных групп, биографии, 
  наградные листы подпольщиков. Фонд комиссии формировался не только за счет документов: 
  членами комиссии пополнили его партизанскими газетами, листовками, стихами и 
  песнями о войне. <br>
  <br>
  Кроме того, был собран богатейший фотоматериал: около 600 фотографий о подпольно-партизанском 
  движении в Крыму.<br>
  <br>
  Из собранных комиссией документов на бумажной основе сформировано 248 дел (без 
  учета фотодокументов). Фонд комиссии был передан на хранение в партийный архив 
  Крымского обкома партии. Фотодокументы не были описаны, их учет велся по журналу, 
  в котором фиксировалось поступление фотографий. Впоследствии они вообще были 
  изъяты из архивного фонда Комиссии по истории Великой Отечественной войны. В 
  настоящее время эти фотографии описаны и включены в Коллекцию фотодокументов 
  по истории Крыма, хранящуюся в Госархиве в АР Крым.<br>
  <br>
  22 сентября 2007 года, в День партизанской славы, архивисты впервые экспонировали 
  фотографии, собранные 60 лет назад членами комиссии, в читальном зале архива. 
  Выставка вызвала интерес у ученых и краеведов.<br>
  <br>
  В этом номере журнала мы начинаем публикацию фотолетописи партизанского движения 
  в Крыму 1941–1944 годов.<br>
  <br>
  <em>Л. П. Кравцова, историк-архивист</em><br>
