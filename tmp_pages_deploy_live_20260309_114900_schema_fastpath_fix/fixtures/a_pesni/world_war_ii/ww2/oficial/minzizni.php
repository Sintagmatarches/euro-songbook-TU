Пусть тяжелы недели, <br>
Живу мечтой о дне: <br>
Ты в офицерской шинели <br>
Скоро придешь ко мне, <br>
Скажешь, как прежде: "Родная!.."<br>
Слезы украдкой блеснут... <br>
Разве у вас не бывает <br>
В жизни подобных минут? <br>
