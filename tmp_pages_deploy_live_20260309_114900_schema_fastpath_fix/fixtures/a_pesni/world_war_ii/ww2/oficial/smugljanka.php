 <em>Новиков А. Смуглянка / Сл. Я. Шведова. М.: Молодая гвардия, 1946.</em><br>
 <br><br>
  Песня написана осенью  1940 г., вскоре после присоединения к СССР Бессарабии и образования Молдавской ССР, в составе сюиты о бессарабских партизанах  Гражданской войны. Сюиту заказало политуправление Киевского военного округа для окружного ансамбля песни и пляски. Авторы посвятили ее Григорию Котовскому. Песни сюиты вошли в репертуар ансамбля, кроме   &quot;Смуглянки&quot;, которая показалась слишком фривольной.<br>
 <br>
 Когда началась Великая Отечестаенная, Новиков предложил песню на радио, но ее отвергли по той же причине. В 1942 г. ура-патриотические песни о войне сменились лирическими, и композитор возобновил попытку. Шведов отредактировал текст, и Новиков отнес &quot;Смуглянку&quot; в числе других песен в ансамбль Александрова. Александрову она понравилась, ансамбль ее выучил, но Госрадио не приняло для записи, в том числе за нереалистичность: молдаванских партизанских отрядов в ходе текущей войны не было, да и вообще в Молдавии  нет условий для партизанской войны.<br>
  <br>
 Песня была впервые исполнена ансамблем Александрова на праздничном концерте 7 ноября 1944 г. в Зале имени Чайковского. Концерт транслировался по радио. Больше в годы войны песню по Госрадио не давали, но фронтовые ансамбли ее подхватили.<br>
  <br>
  Всесоюзно популярной она стала тридцать лет спустя, в 1974 г., когда на экраны вышел фильм Леонида Быкова &quot;В бой идут одни старики&quot;, герои которого - летчики-истребители - поют &quot;Смуглянку&quot;. <br>
  <br>
  Подробнее см. статью внучки Шведова Юлии Гончаровой <a href="a-smuglanku.php">&quot;Смуглянку&quot; не пускали на фронт&quot;</a> (2010). <br>
