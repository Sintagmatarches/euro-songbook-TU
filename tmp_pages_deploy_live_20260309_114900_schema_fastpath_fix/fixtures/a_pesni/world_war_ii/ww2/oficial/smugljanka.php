  <br>
  <strong>Анатолий Григорьевич Новиков</strong> (1896-1984) <br>
  <strong>Яков Захарович Шведов</strong> (1905-1984)<br>
  <br>
  <br>
  <strong>Судьбы Молдавии </strong><br>
  <br>
  Молдавия - историческая область, ранее княжество со столицей в Яссах, в 1859 
  г. объединившееся с Валахией в Румынское княжество - а потом королевство; на 
  рубеже 1918-19 гг. королевство присоединило от Венгрии третью  часть - Трансильванию. 
  Окраинная область исторической Молдавии - Бессарабия - досталась в 1812 г. России. 
  В марте 1918 г. местные националисты  пригласили 
  в Бессарабию румынские войска, и область вернулась к Румынии.<br>
  <br>
  В ходе Гражданской войны в России была попытка российских красных установить контроль над Бессарабией, а заодно Румынией и Венгрией: 8 мая 1919 г. туда готовилась выступить дивизия 
  атамана Григорьева в борьбе за мировую революцию, но большевики не выполнили 
  обязательств по ее снабжению и, главное, не прекратили террор против крестьянства 
  на Украине, а потому части Григорьева взбунтовались и вместо похода в Румынию 
  двинулись громить большевиков. <br>
  <br>
  Не оставив надежд получить Бессарабию, большевики на правобережье Днестра (этнически 
  смешанном) создали 12 октября 1924 г. плацдарм - бутафорскую &quot;Молдавскую АССР&quot; в составе 
  Украинской ССР. Одним из активнейших сторонников похода на Бессарабию был ее 
  уроженец Григорий Котовский (по одной из версий, его странное убийство в 1925 
  г. – а застрелил его ближайший помощник - было инспирировано румынской разведкой; 
  по другой, его заказало большевистское руководство, чтобы не портить международные 
  отношения или по иной причине). <br>
  <br>
  23 августа 1939 г. был заключен пакт Молотова-Рибентроппа, и Румыния разделила судьбу 
  Польши: 28 июня 1940 г. она была вынуждена отдать Бессарабию СССР (после этого там 
  проведены массовые депортации населения), а сама в сентябре была превращена 
  в марионеточное фашистское государство. 2 августа 1940 г. Молдавская 
  АССР и Бессарабия слиты в Молдавскую ССР. Для нее придумали 
  новую социалистическую нацию - молдаван - и  поменяли алфавит на кириллицу. В 1941 г. территория Молдавской ССР занята румынскими 
  и германскими войсками - иначе говоря,  Бессарабия вернулась в состав Румынии, а в 1944 г. занята 
  Красной Армией и осталась в составе СССР. С 1991 г. Молдавия – независимое 
  государство, однако бывшая Молдавская АССР 
  - современное Приднестровье с его преимущественно русско-украинским населением, поддерживаемое 
  российским руководством и российскими военными, отказывается признавать 
  власть Кишинева.<br>
