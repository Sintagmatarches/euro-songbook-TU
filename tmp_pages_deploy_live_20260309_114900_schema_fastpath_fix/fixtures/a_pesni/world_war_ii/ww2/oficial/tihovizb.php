<h1>ТИХО В ИЗБУШКЕ</h1>

Музыка Бориса Фомина<br>
Слова Павла Германа<br>
<br>
Спит деревушка. <br>
Где-то старушка <br>
Ждет не дождется сынка. <br>
Сердцу не спится, <br>
Старые спицы <br>
Тихо дрожат в руках. <br>
Ветер уныло гудит в трубе, <br>
Песню мурлыкает кот в избе. <br>
Спи, успокойся, <br>
Шалью укройся, <br>
Сын твой вернется к тебе. <br>
<br>
Утречком ранним <br>
Гостем нежданным <br>
Воин вернется домой, <br>
Крепко обнимет, <br>
Валенки снимет, <br>
Сядет за стол с тобой. <br>
Будешь смотреть, не спуская глаз, <br>
Будешь качать головой не раз, <br>
Тихо и сладко <br>
Плакать украдкой, <br>
Слушая сына рассказ. <br>
<br>
Ласково солнце <br>
Глянет в оконце, <br>
Радугой вдруг оживет <br>
Жизнь фронтовая,<br>
Даль голубая, <br>
Где протекал полет.<br>
Глянешь на сына разок-другой: <br>
Летная куртка и бровь дугой! <br>
Тихо прижмешься <br>
И улыбнешься - <br>
Не пропадет, мол, такой. <br>
<br>
Тихо в избушке, <br>
Дремлет старушка, <br>
Мысли ее далеко. <br>
В маленьких спицах <br>
Отблеск зарницы, <br>
Светлая даль снегов. <br>
Ветер уныло гудит в трубе, <br>
Песню мурлыкает кот в избе. <br>
Спи, успокойся, <br>
Шалью укройся, <br>
Сын твой вернется к тебе. <br>
