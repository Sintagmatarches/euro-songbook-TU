  В дивизионной историографии боевой путь второго формирования 229-й СД заканчивается 
  таким текстом: <br>
  <br>
  «… Точные цифры потерь дивизии нам неизвестны. Потому что большинство вышедших 
  из окружения в первые дни попали в другие соединения. Так, батальон связи был 
  передан 112-й СД. Бойцы и командиры артполка переданы 62-й армии. Медсанбат, 
  по рассказам, долгое время находился в подполье, в тылу у немцев, а потом вышел 
  и влился в другую часть…»<br>
  <br>
  По понятным причинам лейтенант Черников не писал и не намекал даже, что многие 
  из 229-й дивизии оказались в немецком плену. Наивно звучит и сообщение о 380-м 
  медсанбате, который «долгое время находился в подполье, в тылу у немцев». На 
  страницах книги «Запрещенные солдаты» есть рассказы бывших военнослужащих медсанбата-380: 
  врачей, медсестер, санинструкторов, что выносили раненых с поля боя, а потом 
  вместе с ними оказались в плену. Многие из них попали в женский концентрационный 
  лагерь Равенсбрюк и провели там три долгих года, испытывая нужду, издевательства 
  эсэсовцев. А дома несколько десятилетий жили под бдительных оком спецслужб.<br>
  <br>
  Последний день 229-й дивизии, второго формирования, нигде официально не зафиксирован. 
  Первый называют точно: 12 декабря 1941 года, а о последнем молчат. Имеющиеся 
  в нашем распоряжении материалы позволяют назвать этот день - 10 августа 1942 
  года. Он записан во множестве фильтрационных дел и трофейных карточек: «Wann 
  und wo gefangen?» - когда и где попал в плен? Дата одна и та же, меняются только 
  названия хуторов и станиц - Суровикино, Чир, Нижне-Чирская...<br>
  <br>
  «… Утром на восходе солнца мы услышали гул моторов и увидели идущие прямо на 
  нас по пшенице немецкие танки. Танкисты, стоя в открытых люках, показывали своей 
  пехоте, где прячутся русские солдаты, которых собрали в плен. У нас забрали 
  оружие и погнали в село… А еще ночью к нам прибежал командир 1-й роты и сказал, 
  что надо стреляться. Но наш ротный оборвал его, чтобы не сеял панику. Здесь 
  же была девушка-медсестра. Она заплакала и просила ее застрелить. Утром я увидел, 
  что эта девушка убита выстрелом в лоб, но кто из командиров ее убил, я не знаю. 
  Она осталась лежать в пшенице, а нас около трех тысяч человек загнали в овраг, 
  опутанный в один ряд колючей проволокой…» (Из фильтрационного дела В.Г. Васильева, 
  рядового 783-го СП 229-й СД, Омутинский район).<br>
  <br>
  Штаб 62-й армии, практически разбитой к этому времени, и сам штаб Сталинградского 
  фронта об этой трагедии имели смутное представление. Историки Сталинградской 
  битвы утверждают, что именно «катастрофа, постигшая 62-ю армию, заставила советское 
  верховное командование пересмотреть свое мнение о возможностях генерал-лейтенанта 
  Гордова руководить фронтом на стратегически важном направлении». 9 августа 1942 
  года в 23.00 была подписана директива Ставки № 170562, согласно которой с шести 
  часов утра 10 августа Сталинградский фронт подчинялся командующему Юго-Западным 
  фронтом генерал-полковнику Еременко.<br>
  <br>
  Ну, а как же за Доном, где в окружении остались бойцы и командиры 33-й гвардейской 
  и 181-й, 147-й и 229-й стрелковых дивизий? О событиях тех дней и часов мы знаем 
  по-прежнему очень мало.<br>
  <br>
  «… 181-я и 33-я к 8.00 10 августа вели ожесточенные бои в окружении противника 
  в районах высоты 163.0 и высоты 134.4. Дивизии ощущают острую нужду в боеприпасах. 
  Офицер связи штаба армии капитан Панкин, высадившийся на рассвете в районе ст. 
  Качалинская, в сев. группу не прибыл… 229-я и 147-я СД (южная группа) о себе 
  не сообщили. Связь с дивизиями на правом берегу - радио, которое работает неустойчиво…» 
  (из Боевого приказа № 057, ШТАРМ 62)<br>
  <br>
  «… армия в течение дня продолжала оборонять вост. берег р. Дон, одновременно 
  силами 33-й гв., 181-й, 229-й и 147-й СД вела бои на зап. берегу р. Дон по уничтожению 
  противника, ставшего на пути отхода на вост. берег р. Дон…» (из оперативной 
  сводки № 81).<br>
  <br>
  В очередных сводках несколько раз появляются номера окруженных дивизий:<br>
  <br>
  «… сведений от 33-й гв., 181-й, 147-й, 229-й СД не поступало, на позывные не 
  отвечали, на прием не появлялись…»<br>
  <br>
  Тот же день, чуть позже: «… о 33-й гв., 181-й, 147-й, 229-й - новых сведений 
  не поступало, в радиосвязь войти не удалось…» (из оперативных сводок № 85 и 
  № 86).<br>
  <br>
  Тем не менее, из сводки в сводку, переносится формулировка, что «дивизии ведут 
  бои в окружении, уничтожая противника на путях отхода».<br>
  <br>
  … 13 августа («… на восточный берег переправилась разведка 147-й СД во главе 
  с лейтенантом Левашевым»… «точное положение 229-й дивизии не установлено»)… 
  14 августа («... выход отдельных бойцов и мелких групп из состава дивизий, связь 
  установить не удалось»)… 15 августа («… новых данных о положении окруженных 
  дивизий не поступало»)… 16 августа из окружения вышел командир 33-й гв. СД полковник 
  Утвенко с группой в 120 чел. (из оперативной сводки № 97).<br>
  <br>
  Сведений о судьбе командира 229-й дивизии полковника Федора Сажина нет. В ряде 
  материалов утверждается, что «командир 229-й стрелковой дивизии погиб 10 августа 
  у хутора Пятиизбянский». Но свидетелей его гибели не называют, место захоронения 
  неизвестно.<br>
  <br>
  Некоторые сведения мог бы пролить бывший начальник особого отдела дивизии, старший 
  лейтенант госбезопасности И.С. Лукельев. Он, как сообщается в официальном ответе 
  Омского управления ФСБ от 28 мая 2009 года, с группой бойцов вышел из окружения 
  и продолжил службу в органах военной контрразведки. Вероятно, его сообщение 
  и легло в основу этого ответа.<br>
  <br>
  … Получив приказ командующего 62-й армией о выходе к реке Дон в районе переправ, 
  «подразделения 229-й дивизии следовали на грузовых машинах (так в тексте. - 
  Р.Г.) по маршруту: пос. Зрянинский - совхоз № 5 - Самодуровка… В пяти километрах 
  от пос. Зрянинский завязался бой. Сотрудники отдела заняли оборону в балке, 
  что в двух км восточнее балки Водяная-2»… Около часа дня 10 августа, сообщает 
  Лукельев, чтобы избежать окружения, по приказу командира дивизии разбились на 
  две группы. Первая группа, в которой находились командир дивизии и начальник 
  особого отдела, вышла из балки на возвышенность. В результате боев группа разделилась, 
  та часть, где оказался начальник особого отдела дивизии Лукельев И.С., вышла 
  из окружения.<br>
  <br>
  На полевой карте 76-й пехотной дивизии немцев, которую мы напечатали в предыдущей 
  публикации, у поселка Зрянинский нарисован жирный кружок. Может быть, здесь 
  группа Сажина наткнулась на заслон немецких частей, была остановлена и окружена, 
  а в результате боя - рассеяна?<br>
  <br>
  Но о судьбе командира не упоминается ни в одном известном нам документе. Судьба 
  других старших командиров 229-й, за исключением комиссара Тихона Бандурина, 
  известна. Они, по словам начата 229-й дивизии подполковника Манько, были все 
  вместе еще перед Зрянинским. При отходе дивизия попала под оборонительный огонь 
  524-го истребительного полка немцев, а у высоты 156.0 - под огонь артиллерии 
  297-й пехотной дивизии. Два наших полка, 783-й и 804-й, смогли частично пробиться 
  через немецкие позиции у Зрянинского. А 811-й полк, в котором находился подполковник 
  Манько, вынужден отойти назад. В глубокой балке севернее высоты 96.0 полк подвергся 
  внезапной атаке того же 524-го истребительного полка. Вместе с бойцами попал 
  в плен и подполковник Манько. Оказался в плену и начальник штаба дивизии подполковник 
  Мелешкевич. О нем подробно рассказывалось в очерке Александра Петрушина «Дивизии, 
  пропавшие без вести».*<br>
  <br>
  На западном берегу Дона оказались в окружении четыре стрелковые дивизии и четыре 
  комдива. Полковник Утвенко вывел часть своих бойцов и получил приказ 19 августа 
  занять оборону на новом рубеже. Командир 181-й дивизии генерал-майор Новиков 
  взят в плен 15 августа, погиб в плену в декабре 1944 года. Полковник Сажин, 
  комдив-229, как принято считать, погиб 10 августа. Трагичной оказалась судьба 
  командира 147-й дивизии генерал-майора Александра Вольхина.<br>
  <br>
  Как известно, он вышел на левый берег Дона с небольшой группой солдат - 27 человек. 
  Сохранился доклад генерала военному совету 62-й армии.<br>
  <br>
  Из него следует, что 229-я и 147-я дивизии, согласно приказу штаба армии, должны 
  были пробиваться на восток вместе. По словам комдива-147, после ночного боя 
  у высоты 102.3, войска соединений смешались. Поскольку за 229-ю заступиться 
  некому, командир 147-й то и дело подчеркивает, что «811-й полк, видимо, снова 
  свой участок оставил без боя…», указывает на «тяжелое и паническое состояние 
  тылов 229-й, которые мчались с востока по большаку…» и тому подобное.<br>
  <br>
  «Я решил днем никаких перегруппировок не делать, дождаться ночи, соединить все 
  части группировки и лично выводить в следующую ночь», - пишет в докладе Вольхин. 
  Не получилось: утром немцы окружили всех, кто находился в балке Грачевой. Несколько 
  часов шел бой. Надеясь все-таки перейти в соседнюю, более приспособленную к 
  обороне балку 2-я Водяная, генерал подробно перечисляет силы, на которые он 
  мог бы рассчитывать. Но «… 15-й полк - связь с ними установить не смог… 600 
  полк - связь установить не смог… 645-й танковый батальон - куда ушел, не знаю, 
  связь с ним установить не смог. В таком положении я сам потерял связь с дивизией…»<br>
  <br>
  Я цитирую подробно доклад не для того, чтобы осудить командира одного соединения 
  и обелить другого. Эти отрывки помогут читателю представить суматоху и отчаяние, 
  которые царили в гибнущих частях - без боеприпасов, без связи, рассеянных в 
  августовской степи, под вражеским огнем…<br>
  <br>
  Генералу Вольхину поставили в вину все, что случилось на левом берегу Дона. 
  Один из авторов утверждает: «Высшее руководство рассуждало так: раз Вольхин 
  не погиб, значит, не все сделал для победы». В тот же день, когда генерал представил 
  доклад военному совету армии, было принято заключение, в котором сказано, что 
  комдив «трус, позорно бежал с поля боя»… А еще через два дня Еременко и Хрущев 
  отдали его под суд военного трибунала.<br>
  <br>
  Трибунал обвинил генерала в «измене Родине в форме перехода на сторону врага». 
  Потом был приговор - к высшей мере. Потом несколько месяцев в ожидании расстрела. 
  И… - направление в штрафной батальон. Рядовым.<br>
  <br>
  … 31 декабря 1942 года рядовому Вольхину присвоили звание майора. В январе 1943 
