  <br>
  Что же на самом деле происходило в окруженных частях 229-й дивизии? Об этом 
  рассказывают выдержки из документов, которые хранятся в фильтрационных делах 
  попавших в плен бойцов ишимской дивизии. Но хотелось бы сделать одну оговорку. 
  В каждом деле называются, казалось бы, точные числа, когда происходили роковые 
  для дивизии и самого солдата события. Они записаны со слов и не всегда точны. 
  Думается, что в сплошных боях, когда день перемешан с ночью, у человека теряется 
  ощущение времени. Написал же участник штурма рейхстага капитан Василий Ярунов, 
  родом из села Богандинского Тюменского района, на обороте карточке, снятой у 
  входа в только что поверженный рейхстаг: «31 апреля»!..<br>
  <br>
