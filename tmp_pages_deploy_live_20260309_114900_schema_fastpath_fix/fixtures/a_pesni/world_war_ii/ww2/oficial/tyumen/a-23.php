  все штабные сожгли, а потом попали в плен, из плена мне приходилось убегать 
  два раза, три, но никак не мог пройти через линию фронта...» Так излагал свой 
  личный эпизод Алексей Андреев из Казанского следователю контрразведки «СМЕРШ».<br>
  <br>
  Судя по его рассказу, надежда поисковиков найти в одной из задонских балок закопанные 
  9-10 августа документы 229-й дивизии - пустая. Нельзя вопрошать давно рассеянный 
