  быть больше товарищеского чувства к своим окруженным частям, чем у немецко-фашистского 
  командования. На деле, однако, оказывается, что советское командование проявляет 
  гораздо меньше заботы о своих окруженных частях, чем немецкое. Это кладет пятно 
  позора на советское командование. Ставка считает делом чести нашего Сталинградского 
  командования спасение окруженных частей. У нашего Сталинградского командования 
  имеется сейчас достаточно сил и средств, чтобы пробиться к своим окруженным 
  дивизиям и вывести их.<br>
  <br>
  Ставка приказывает немедленно организовать прорыв фронта противника, пробиться 
  к своим окруженным дивизиям и организованно вывести их. О принятых мерах донести». 
  Эту директиву Ставки N 170569 подписал Сталин. 15 августа 1942 г… Когда перечисленные 
  в документе дивизии уже погибли…<br>
  <br>
  Но мы опять забежали вперед. В нашем рассказе еще должен быть вечер 9 августа. 
  229-я дивизия, все, что осталось от ее пехотных полков, ее артиллерии, ее тылов 
  и медицинской службы, ее учебного батальона, батальона связи и роты химзащиты, 
  сосредоточилось в глубоких балках перед хутором Тузов. Они двигались в направлении 
  высоты 156.0, намереваясь повернуть и выйти к Дону в районе хутора Пятиизбянский. 
  Движение это было сразу замечено, немцы начали обстрел колонн. Заградительный 
  огонь заставил остановиться и залечь 804-й полк, следующий СП-783 смешался за 
  ним. По схеме, реконструированной Юрием Власовым, 804-й и 783-й полки сумели 
  повернуть по намеченному маршруту. 811-й полк вынужден был отойти назад, к балке 
  Водяная-2. Сажин распорядился разъединиться. Он поручил майору Крозу вывести 
  батальон связи, и тому это удалось. И только к вечеру 13 августа батальон связи 
  - 160 человек - вышел из окружения. Сам Сажин с учебным батальоном стал прикрывать 
  отход…<br>
  <br>
  «… Не сумев пробиться, мы повернули на город Калач, но отойдя от Суровикино 
  15 км, узнали, что этот город занят немцами. Тогда мы стали выходить из окружения 
  кто как мог. Я пошел с группой в 120 человек. С нами были командир и комиссар 
  дивизии. 10 августа нас заметили немцы и обстреляли из минометов. Меня ранило 
  в ногу, и я остался лежать в поле…» (Из фильтрационного дела А.В. Чупина, мл. 
  сержанта 130-го зенитно-артиллерийского дивизиона 229-й СД, Викуловский район)<br>
  <br>
  «… 10 августа в тридцати км от г. Калач наш учебный батальон 229-й СД в количестве 
  200 с лишним человек был окружен немцами и пленен. При пленении мы сдали немцам 
  имевшиеся у нас винтовки». (Из фильтрационного дела И.Н. Попкова, рядового 811-го 
  СП 229-й СД, Казанский район)<br>
  <br>
  «… До 10 августа наш полк держал оборону левее ст. Суровикино. Немцы из миномета 
  ранили меня осколками в левую ногу, а потом окружили и взяли в плен вместе с 
  другими бойцами взвода. Тяжелораненых немцы застрелили, а меня поддержали за 
  руки мои товарищи…» (Из фильтрационного дела П.И. Сухинина, рядового 811-го 
  СП 229-й СД, Вагайский район)<br>
  <br>
  … Принято считать, что группа Сажина сдерживала немцев целые сутки и что сам 
  полковник погиб в бою в районе хутора Пятиизбянский, километрах в двадцати восточнее 
  Тузова.<br>
