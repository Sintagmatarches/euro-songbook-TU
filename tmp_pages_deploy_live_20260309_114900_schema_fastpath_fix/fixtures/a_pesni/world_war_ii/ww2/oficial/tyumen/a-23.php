	<strong>Рафаэль Гольдберг </strong><br>
  <br>
	<span class="header"><strong>ДВАДЦАТЬ ТРЕТЬЕГО ГОДА РОЖДЕНИЯ<br>
  История 229-й стрелковой дивизии, второе формирование </strong><br></span>
  <br>
  
  <em>&quot;Тюменский курьер&quot;, 22, 23, 27, 29 января, 3 февраля, №№ 9(2783), 
  10(2784), 12(2786), 14(2788), 17(2791) - проект &quot;Охота за тенью&quot;.</em><br>
  <br>
  <br>
  <em><strong>Похоронен под красной звездой </strong><br>
  <br>
  После публикации 17 ноября в газете «Заводоуковские вести» материала о воинах-земляках, 
  попавших в фашистский плен, чьи судьбы по сей день остаются белыми пятнами в 
  историографии Великой Отечественной войны, в редакцию стали поступать отклики.<br>
  <br>
  Одной из первых позвонила Лидия Карловна Сихварт из Падуна.<br>
  <br>
  - Прочитала в газете о Батянове Германе Павловиче, плененном немцами в августе 
  1942 года. Не все, но сходится: год рождения, и что из Падуна, а главное - мать 
  Батянова - Фекла. Эту бабушку я знала. Сразу подумалось о здравствующей старшей 
  сестре Германа Вере. Ей уже девяносто. Да я и сама довожусь ему родней, потому 
  как замужем была за его племянником, ныне покойным. А вот фамилия напечатана 
  не так, правильно будет - Батенев. Имя тоже другое, вернее, стало другим. Рос 
  и воевал он, будучи Германом, но после войны стал Григорием. Почему? Об этом 
  и остальном лучше меня сестра его расскажет. Приезжайте.<br>
  <br>
  … И вот я в Падуне, в доме Веры Павловны Ивлевой. Вместе с проводившей меня 
  сюда Лидией Карловной, частенько навещающей престарелую родственницу, слушаем 
  ее воспоминания о брате. Годы все же сказываются. Вере Павловне неможется, а 
  потому говорит, не вставая с кровати. Вспоминает она, перескакивая, что называется, 
  с пятого на десятое, некоторые даты путает, иные - не помнит вовсе. Но с помощью 
  уточняющих и наводящих вопросов, да подсказок Лидии Карловны удалось более-менее 
  складно записать ее рассказ.<br>
  <br>
  - Как война началась, Грише семнадцать с чем-то было, тут его сразу и угнали 
  на войну. Поначалу на связиста учился. В лесу под Ишимом их часть стояла. Я 
  к нему ездила проведать. Оттуда и направили его на фронт. Отбыл, как пропал. 
  Три года ни слуху, ни весточки от него. Отец-то наш болел шибко, лежал больной 
  и все время говорил: попомните меня, вернется наш Ернюшка, ничто ему не сделается. 
  Так и вышло. Война окончилась, и вскоре письма от него пришли - одно, второе, 
  а там и сам заявился.<br>
  <br>
  Рассказывал, что был в плену, все три года на богатых немцев батрачил. Повезло: 
  иные хозяева наших пленных, как скот, в хлевах содержали, а с ним по-божески 
  обходились - почти не били и кормили сносно. Как в плен попал? Сказывал, дивизию 
  их немцы окружили, прижали к реке. Большинство красноармейцев погибло, а оставшихся 
  - пленили.<br>
  <br>
  Он, посеченный осколками, - веко было нарушено и пальцы руки, - тоже оказался 
  в подвалах, где от скопления людей - не продохнуть. Сущий ад! Держали без еды 
  и питья. Многие умирали, тех, кто бежать пытался, расстреливали тут же. Затем 
  запечатали всех в вагоны и повезли в Германию. А там - кому в концлагерь дорога, 
  а кто, как он, к бюргерам в работники попал. Приезжали немцы, выбирали их, приценивались, 
  словно к рабам.<br>
  <br>
  … Освободили Григория союзники, или, как назвала это Вера Павловна, «Америка 
  до него первой доехала». Возвращался домой не без боязни. Наслышан был об отношении 
  на родине к вчерашним военнопленным. Но и тут повезло. Не раз вызывали Григория 
  в Новую Заимку на допросы в местный отдел НКВД, однако отступились. И все же 
  от греха подальше перебрался он в Тюмень, устроился водителем, а там и семьей 
  обзавелся. Проживает ныне в Тюмени его сын Сергей, растут внуки.<br>
  <br>
  - Но почему все-таки Григорий, а не Герман, как значилось в немецкой карточке 
  военнопленного?<br>
  <br>
  - Германом его родители назвали, а он еще маленьким имя это не терпел. Видать, 
  из-за того, что дразнилка прицепилась - Ернюшка да Ернюшка. Когда освободился 
  из плена, имя-то сразу и сменил. Первое письмо пришло от него, подписанное Григорием 
  Павловичем. Что за Григорий? - думаем. - У нас в роду одного из дядьев так звали, 
  но не от него ведь письмо. А в следующем письмеце наш Ернюшка объяснился, почему 
  он теперь Григорий. В плену его «фашистское» имя Герман, да еще как у одного 
  из их главарей - Геринга, стало ему прямо-таки ненавистным…<br>
  <br>
  Умер Григорий Павлович Батенев в 2000 году. Похоронен в Тюмени. Как вспоминает 
  Вера Павловна, погребен в ранге участника Великой Отечественной войны. При содействии 
  и помощи военкомата ему установлен надгробный памятник с красной звездой.<br>
  <br>
  Александр Парамонов<br>
  <br>
  <br>
  <strong>Редактору газеты «Заводоуковские вести» Е. Пономаревой</strong><br>
  <br>
  Уважаемая Елена Григорьевна! Я нашел «вашего» Батенева в списках новобранцев 
  229-й стрелковой дивизии. Правда, его фамилии там тоже не повезло. В «именном 
  списке на команду N... отправляемую из Ново-Заимского райвоенкомата» 18 декабря 
  1941 года, он значится под № 13 как Беженев Герман Павлович, 1923 года рождения. 
  Приказом № 9 параграф 2 все 18 человек, все 1923 года, отправлены в полковую 
  школу в пулеметный взвод.<br>
  <br>
  О том, что случилось с 811-м стрелковым полком, в котором воевал Григорий Павлович, 
  я расскажу в своем комментарии к вашей заметке. Р.Г.<br>
  <br>
  <br>
  <strong>«Батянов Герман Павлович. 1924 г.р., д. Падун, бывшего Ново-Заимского, 
  ныне Заводоуковского района. Рядовой 811-го СП 229-й СД. Призван Ново-Заимским 
  РВК 18 декабря 1941 г. Попал в плен 1 августа 1942 г. на правом берегу р. Дон. 
  Мать - Батянова Фекла, жила в д. Падун.»</strong><br>
  <br>
  Из рукописи четвертого тома книги Запрещенные солдаты»</em><br>
  <br>
  <br>
  <strong> ... Еще почти не жили </strong><br>
  <br>
  На моем рабочем столе лежит большой пластиковый конверт размером в лист писчей 
  бумаги. Я стараюсь лишний раз его не открывать, хотя, казалось бы, ничего необычного 
  в запечатанном конверте нет. И все-таки, повторюсь, лишний раз я в него не заглядываю.<br>
  <br>
  Это привезенные нашими поисковиками из Подольского архива Министерства обороны 
  списки мобилизованных в конце 1941 года из деревень и сел нынешнего тюменского 
  юга и отправленных командами в Ишим на формирование будущей 229й стрелковой 
  дивизии. Подробность: почти все они родились в один и тот же 1923 год. И все 
  они приняли участие в боях 229-й дивизии на правом берегу Дона, пытаясь преградить 
  движение 6-й полевой армии генерала Фридриха Паулюса к Сталинграду. И почти 
  все они там и остались, на правом берегу. Кто в числе убитых, останки которых 
  до сих пор собирают за Доном поисковые отряды, что нашли прошлым летом все, 
  что осталось от рядового Степана Кузнецова из деревни Каньга Ялуторовского района, 
  и его смертный медальон в придачу. А кто оказался в плену, потом погиб на пути 
  в лагерь военнопленных или в уже самом лагере, одном из множества их на территории 
  самой Германии или захваченных ею в ходе блицкрига европейских стран.<br>
  <br>
  Ровные колонки повторяющихся цифр в списках напоминают солдатские шеренги. И 
  мы до сих пор еще точно не знаем, кто из них остался в строю только в списках 
  229-й дивизии. Зато знаем, сколько их осталось в живых в августе 1942-го - на 
  левый, восточный берег Дона вырвались 750 бойцов и командиров. А когда дивизия 
  28 сентября 1942 года «убыла на новое формирование», уже третье с начала войны, 
  в Волоколамский район Московской области прибыло 169 человек.<br>
  <br>
  Но в декабре 1941-го эти восемнадцатилетние мальчики, направляясь в Ишим, еще 
  ничего этого не знали. Не знали даже, куда их точно направляют. То ли из соображений 
  секретности, то ли по иной причине командировали мобилизованных, «удовлетворив 
  денежным довольствием на двое суток», в распоряжение начальника 2-й Ленинградской 
  авиашколы полковника Никифорова. Была такая школа переброшена в тыловой Ишим 
  в самый канун войны. Впрочем, вряд ли кому-нибудь из ребят мечталось попасть 
  в летную часть. На именных списках, так называются эти листки, сохранились карандашные 
  пометки: стрелковый взвод, команда станковых пулеметчиков, команда минометчиков…<br>
  <br>
  Сведения об историческом пути 229-й отрывочны и порою недостоверны. История 
  писалась уже после войны на основе документов и воспоминаний тех, кто оказался 
  в третьем формировании этой дивизии, кто дожил до победы. А документы ишимского 
  формирования не сохранились. По утверждению начальника штаба дивизии подполковника 
  Мелешкевича, он лично закопал все документы в районе села Жарки Сталинградской 
  области перед последней попыткой прорвать кольцо окружения.<br>
  <br>
  Правда, в своих воспоминаниях лейтенант Черников, сотрудник политотдела СД-229, 
  утверждал, что «боевая жизнь дивизии началась под Смоленском», который она защищала 
  около месяца.<br>
  <br>
  Однако в документах 20-й армии, относящих еще к июлю 1941 года, соединение с 
  таким номером упоминается. Оно входило в состав 69-го стрелкового корпуса и 
  отражало атаки наступающих немецких частей в районе г. Витебск. Так, 9 июля 
  1941 года… 229-я СД оставила Сенно, районный центр Витебской области. 10 июля 
  она «ведет бой с превосходящими силами пехоты и танками противника на рубеже 
  р. Оболянка»… В тот же день «выдвигается в сторону Витебска», 811 полку (из 
  той же 229-й) поручается «занять и удерживать восточную половину Витебска»… 
  13 июля противник прорывается на стыке 229-й и 233-й дивизий, но «к 18-30 положение 
  восстановлено, хотя до ста танков остались в тылу нашей обороны, с которыми 
  бой продолжается…»<br>
  <br>
  И еще буквально одна строчка из оперативной сводки Западного фронта. 19 августа 
  1941 года, 20 часов: «229-я дивизия закрепляется на прежнем рубеже…» Это уже 
  смоленское направление. Следовательно, «боевая жизнь дивизии под Смоленском», 
  о которой упоминает лейтенант Черников, действительно имела место.<br>
  <br>
  Но, повторюсь, документального подтверждения нет, воспоминаний об арьергарных 
  боях летом и осенью 1941 года не сохранилось. И потому днем рождения соединения 
  официально считается 12 декабря 1941 года, а местом - город Ишим и станция Называевская.<br>
  <br>
  Деталь: лейтенант Черников утверждает, что «основным пополнением была молодежь 
  19-20-летнего возраста, преимущественно сибиряки, жители Омской области». Наши 
  документы говорят, что ребята были на год-два моложе. Что в дальнейшем, когда 
  229-я, личный состав которой не имел никакого боевого опыта, встала на пути 
  одной из самых опытных в германских сухопутных войсках 6-й полевой армии под 
  командованием Фридриха Паулюса, не могло не сыграть свою роль.<br>
  <br>
  <img src="a-23-1.jpg" width="720" height="476"><br>
  <br>
  <strong>Ишим - большая излучина Дона </strong><br>
  <br>
  Итак, первое формирование 229-й стрелковой дивизии начало войну под Витебском 
  и, то отступая, то контратакуя, прошло с боями до Смоленска. Потери в людях 
  оказались настолько велики, что с 12 декабря 1941 года пришлось начать, фактически, 
  с нуля второе формирование соединения с тем же номером. Происходило это, напомним, 
  на юге Западной Сибири - в городе Ишиме и на станции Называевская.<br>
  <br>
  ... Первые три месяца новым формированием 229-й руководил командир, «отсталый 
  в военном отношении». Только 10 марта в Ишим прибыл полковник Федор Сажин, кадровый 
  военный, 22 года в Красной армии. С начала войны Сажин командовал полком, который 
  получил звание гвардейского, затем - стрелковой бригадой. Но сроку на подготовку 
  к боям было отпущено немного. В двадцатых числах апреля 1942 года дивизия погрузилась 
  в вагоны, сдав, согласно приказу комдива, все артиллерийское, учебное и боевое 
  оружие. При себе осталось личное оружие (пистолеты и револьверы), противотанковые 
  ружья и боевые винтовки - от 12 до 24 штук на полк. И по сорок винтовочных патронов 
  на каждую. С тем и отбыли - три стрелковых полка (783-й, 804-й, 811-й), один 
  артполк, противотанковый и зенитно-артиллерийский дивизионы, учебный, саперный, 
  медико-санитарный батальоны и отдельный батальон связи.<br>
  <br>
  В мае дивизия была уже в городе Скопин Рязанской области, где получила полное 
  вооружение, а в июне комиссия наркомата обороны проверила состояние подготовки 
  и признала дивизию кадровой. Потом дивизия прибыла в Сталинград, оттуда в пешем 
  строю - 150 километров - к своему первому и последнему сражению в районе населенного 
  пункта Суровикино.<br>
  <br>
  Не станем заново пересчитывать роковые и ошибочные события весны и лета 1942 
  года, которые позволили немецким войскам, действовавшим на юге России, выйти 
  на оперативный простор и, согласно плану «Брауншвейг», устремиться к Волге и 
  Сталинграду. В большой излучине Дона на оси этого броска оказались три стрелковые 
  дивизии. Слева 112-я, справа - 147-я и в центре, прикрывая железную дорогу, 
  наша 229-я.<br>
  <br>
  На левом фланге - 811-й полк, в центре - 783-й, затем 804-й, но не в полном 
  составе. Два его батальона отстали в пути и не успели занять намеченные рубежи.<br>
  <br>
  «... 25 июля наш 804-й полк форсировал Дон (с востока на запад. - Р.Г.) и пошел 
  по направлению к станице Нижне-Чирской. 2-й батальон ушел вперед, а 1-й и 3-й 
  шли за ним походным строем. 26 июля в районе станицы Нижнее-Чирской наша колонна 
  в составе двух батальонов была окружена автоматчиками и танками. Вся колонна 
  была пленена, в том числе и я...» (Из фильтрационного дела бывшего военнопленного 
  Г.Д. Положкова, ст. сержанта 804-го СП, с. Юрга)<br>
  <br>
  Вместе с тем, «дивизионная историография» утверждает, что «сражение дивизии 
  с немцами началось 22 июля и закончилось 10 августа, когда дивизия вышла из 
  окружения». Как протекало это сражение, в каком виде дивизия вышла из него, 
  мы попытаемся рассказать, опираясь на скупые оперативные сводки штаба Сталинградского 
  фронта, штаба 62-й армии и воспоминаний непосредственных участников событий.<br>
  <br>
  ... Развернутые по фронту на расстояние свыше двадцати километров полки 229-й 
  по-разному переживали события этих двух с лишним недель. Центральное направление, 
  на котором путь немцам к Дону преграждал 783-й полк майора Рыбакова, было и 
  самым трудным. Фашисты хотели решить двоякую задачу: лишить поддержки обороняющиеся 
  дивизии и выйти к Дону кратчайшим путем. Именно сюда, на позиции второго батальона, 
  обрушился первый удар танков и пехоты. Немцев удалось остановить. Но ненадолго, 
  пока не подошли главные силы противника. А 24 июля бой шел уже по всей линии 
  обороны дивизии. По сводке, только против Рыбакова были брошены два пехотных 
  полка и до 60 танков. Но за двое суток врагу продвинуться здесь не удалось. 
  Зато немцы потеснили 804-й полк - на правом фланге. Они прорвались к реке Чир...<br>
  <br>
  Где-то в этих местах через шесть с лишним десятков лет поисковый отряд «Уран» 
  из Волгограда откопает засыпанную взрывом стрелковую ячейку. Вместе с останками 
  погибшего солдата поднимет черный пластмассовый медальон, который удастся прочесть 
  в нашей редакции. Медальон рядового из 811-го стрелкового полка Степана Кузнецова, 
  того же самого года рождения, 1923-го, из деревни Каньга Ялуторовского района.<br>
  <br>
  Немцы прорвались к реке Чир и зашли во фланг полку майора Рыбакова. Фланговые 
  атаки отбивал артиллерийский дивизион старшего лейтенанта Бондаренко. Отбивал, 
  пока хватало снарядов. Потом в дело пошли гранаты. Рассказывают, что сам Бондаренко 
  со связкой гранат бросился под танк...<br>
  <br>
  26 июля дивизия отошла на новые позиции. Вся, кроме 783-го полка. Отходу помешал 
  авиационный налет, который застал этот полк в станице Нижне-Чирской. Потом пошли 
  танки... Только через десять часов обескровленный полк занял место на новых 
  позициях вдоль железной дороги. Теперь уже на правом фланге обороны.<br>
  <br>
  ... Мы не претендуем на абсолютную достоверность в описании того, что происходило 
  в конце июля - начале августа 1942 года в большой излучине Дона, где в значительной 
  степени решалась судьба Сталинграда. Сумятица тех дней, возрастающий напор немецкой 
  танковой громады, погибающие на берегах Дона роты и батальоны, пестрота приказов 
  и чехарда кадровых отставок и назначений... Хотелось бы напомнить, что, возможно, 
  именно эти события легли в основу сталинского приказа N 227, один из пунктов 
  которого делал семьи командиров в самом прямом смысле заложниками поведения 
  в боях...<br>
  <br>
  Да, свидетельства и факты, которые мы получили из различных источников - документы 
  Центрального архива Министерства обороны, протоколы допросов освобожденных из 
  плена солдат 229-й дивизии, написанные уже после войны, материалы, переведенные 
  из немецких архивов, - часто противоречат друг другу. Что же теперь? Ворошить 
  прах и искать? Кто больше прав? Кто виноват в том, что отступили - 112-я, 229-я 
  или 147-я дивизии? Каждый из командиров отстаивал бы свою точку зрения, если 
  бы мог сегодня принять участие в этой дискуссии. Но не нами сказано: у победы 
  много родителей - поражение сирота. Потому мы предложим читателю разные точки 
  зрения, не совпадающие друг с другом факты. Просто за каждой точкой зрения, 
  за каждым мнением стоял конкретный человек, у которого в этом сражении была 
  своя огневая точка, свой взгляд на происходящее и своя, следовательно, оценка...<br>
  <br>
  Вот отрывок из «официальной истории соединения», написанной в конце войны лейтенантом 
  В. Черниковым:<br>
  <br>
  «Бои на новой позиции шли беспрерывно. Дивизия не только упорно защищалась, 
  но нередко и сама переходила в контратаки... 29 июля командующий 64-й армией 
  объявил 229-й благодарность за упорство и стойкость в боях».<br>
  <br>
  Дальше цитируется сообщение Совинформбюро от 30 июля 1942 года: «... на Сталинградском 
  направлении часть под командованием полковника Сажина в результате успешных 
  боевых действий продвинулась вперед, заняв несколько населенных пунктов»...<br>
  <br>
  «... Когда на рассвете переходит в наступление сибирская стрелковая дивизия, 
  сильно поддержанная танками, - начинается ад. Такого минометного огня я еще 
  никогда не испытывал...» - голос человека «с той стороны», строчки из дневника 
  немецкого унтер-офицера 297-го артполка 297-й пехотной дивизии Алоиза Хеймес-сера 
  (убит в Сталинграде в октябре-ноябре 1942 г.).<br>
  <br>
  «Мои дорогие Аничка и Веруся. Я жив и здоров. Вот уже 15 дней горю в жарком 
  бою. Вы, очевидно, по радио слышали, как мои богатыри громят немчуру. Вот уже 
  не стало опасно, и до 50 танков рухнули на поле боя. Можно считать, что час 
  расплаты с врагом близится. Правда, враг еще очень силен и требует много усилий... 
  Я лично и ночь, и день на поле боя, едим и спим на бегу. Ну, мои родные, живите 
  дружно, будьте здоровы. Целую - ваш и вас любящий Федя и папа» - из письма полковника 
  Сажина, 4 августа 1942 г.<br>
  <br>
  Это было последнее письмо полковника. Вряд ли он хотел и мог написать обо всем, 
  что происходило в эти дни с ним, с его дивизией и со всей Красной армией. В 
  том числе и между 26 июля, когда обороняющиеся на правобережье Дона вынуждены 
  были отступить почти к реке, и процитированным выше сообщением Совинформбюро.<br>
  <br>
  Через две недели, утром 10 августа, в одном из оврагов севернее реки Чир был 
  взят в плен начальник артиллерии 229-й дивизии подполковник Манько. Когда окруженные 
  части пытались прорваться через плотное кольцо врага, в надежде выйти к Дону, 
  до которого оставалось еще от двадцати до 30 километров, подполковник Манько 
  находился в боевых порядках 811-го полка. К его показаниям мы еще вернемся. 
  Сейчас я хотел бы процитировать только один абзац из протокола допроса подполковника 
  в отделе I «Ц» 297-й немецкой пехотной дивизии:<br>
  <br>
  «В результате немецкой атаки южнее Чира 25-26 июля у совхо-за-79 дивизия понесла 
  значительные потери и отошла назад на север к Чиру. Следственный процесс, начатый 
  по результатам этих боев против руководства дивизии, был завершен вынесением 
  предупреждения командиру дивизии и командиру артиллерии подполковнику Манько; 
  оба остались на своих местах...»<br>
  <br>
  А 28 июля нарком обороны Сталин подписал приказ N 227, получивший название «Ни 
  шагу назад!» Судя по всему, судьба смилостивилась над полковником Сажиным. Дамоклов 
  меч прика-за-227 не опустился на его голову. Правда, в одном из очерков, посвященных 
  боям на дальних подступах к Сталинграду, утверждается (с ссылкой на документы 
  особого отдела НКВД Сталинградского фронта), что «на Сталинградском фронте этот 
  приказ стал реализовываться на практике с 1 августа». А возможно, кто-то посчитал, 
  что там, между реками Чир и Дон, полковнику гораздо жарче, чем в аду, о котором 
  упомянул в своем дневнике унтер-офицер Алоиз Хеймессер.<br>
  <br>
  Так, во всяком случае, читается между строк «краткой характеристики боевых действий 
  соединения за 1942 год», которую ровно год спустя подписал полковник Соленов, 
  очередной командир 229-й.<br>
  <br>
  «... Дивизия с 22.07.42 по 10.08.42 вела непрерывные тяжелые бои, сдерживала 
  натиск превосходящих сил противника в составе двух моторизованных, одной танковой 
  дивизии и крупных сил авиации, оставаясь на прежних рубежах 62-й армии. За время 
  боев дивизия потеряла 9000 человек и технику: два артдивизиона, две батареи 
  ОИПТД, две батареи 120 мм минометов, один дивизион Резерва главного командования, 
  приданный...»<br>
  <br>
  9000 человек, три четверти личного состава. Если заняться немыслимой по объему 
  работой и сверить именные списки призывников, направленных в Ишим в декабре 
  1941 года (о них шла речь в самом начале нашего очерка), с многотомником «Память», 
  со списками не вернувшихся с войны, которые хранятся в сельсоветах и школьных 
  музеях, то после этого, пожалуй, не захочется задавать наивный вопрос: куда 
  девался генофонд нашего народа? Почему обезлюдели наши деревни и села?<br>
  <br>
  Но впереди еще десять дней боев.<br>
  <br>
