  <br>
  Итак, 8 часов утра 9 августа 1942 года. Оперативная директива № 00235 штаба 
  Сталинградского фронта: «62-й армии - уничтожить части противника, вышедшие 
  на пути отхода армии, и отходить на восточный берег реки Дон, на рубеже которого, 
  совместно со 115-м УР занять и оборонять восточный берег на фронте: Мало-Голубая 
  - устье р. Донская Царица. Штаб армии - с. Карповка. Гордов. Хрущев. Никишев».<br>
  <br>
  У 229-й слитного отхода не получилось. Судьба дивизии стремительно распадалась 
  на тысячи судеб, схожих, но в то же время неповторимых.<br>
  <br>
  Как записано в неоднократно уже нами цитируемой истории дивизии, 229-я получила 
  утром 9 августа приказ выйти из окружения - так следует понимать текст оперативной 
  директивы штаба фронта № 00235: «уничтожить части противника и отходить». Но 
  как сделать это? С шестого по девятое августа, целых три дня немцы накапливались 
  в пространстве между железной дорогой и Доном. Поэтому лейтенант Черников, как 
  человек военный, делает совершенно справедливый вывод: «Речь шла не о прорыве 
  кольца, а о проходе двадцатикилометрового участка, занятого сильнейшим противником, 
  силы которого возрастали с каждым часом». Однако ни штаб фронта, ни штаб армии 
  не могли себе позволить слов, которыми обрисовал ситуацию лейтенант-летописец. 
  Поэтому еще едва ли не целую неделю в приказах и распоряжениях повторяется: 
  «разбить, уничтожить…» А на самом деле, как показали рассекреченные шесть с 
