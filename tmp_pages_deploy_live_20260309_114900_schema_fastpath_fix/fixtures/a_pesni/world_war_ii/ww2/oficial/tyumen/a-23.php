  <em>Полковник Федор Сажин, командир 229-й стрелковой дивизии (архивная копия)</em><br>
  <br>
  Итак, конец июля 1942 г. В истории Сталинградской битвы эти дни - с 31 июля 
  по 17 августа - считаются «завершающим периодом оборонительного сражения Красной 
  армии в большой излучине Дона».<br>
  <br>
  Дивизии 62-й армии, отступив под напором лучшей в немецких сухопутных войсках 
  6-й полевой армии Паулюса, закрепились вдоль насыпи железной дороги, ведущей 
  с запада в Сталинград. Как утверждают источники, яростное сопротивление Красной 
  армии порядком вымотало и немецкие войска. Стало понятно, что силами одной армии 
  Паулюса взять Сталинград сходу не удастся. Немцам пришлось перебросить с кавказского 
  направления 4-ю танковую армию генерал-полковника Гота.<br>
  <br>
  Еще раз повторим: у победы много родителей и летописцев тоже. Поражение - во 
  всех смыслах сирота. Информация о тяжелых боях, которые закончились отступлением, 
  окружением, пленом, собирается по крохам. Вот если бы мог заговорить солдат, 
  силуэт которого украшает одну из площадей Ишима! Но и он - лишь контур бойца, 
  пробивающегося сквозь каменную стену. Нам представляется, что эта стена - стена 
  молчания, стена безвестности.<br>
  <br>
  Собранное нами - часто неясно и противоречиво. Так, в одном из документов автор 
  ссылается, что еще 26 июля, в самом начале оборонительных боев в большой излучине 
  Дона, «немецкие самолеты разбомбили железнодорожный мост у станицы Нижне-Чирская». 
  Между тем, ни на современной карте железнодорожной сети Волгоградской области, 
  ни на «копии с копии» немецкой полевой карты этого же района, с пометкой «feindlage*: 
  7.8.42», нет мостового перехода в районе станицы Нижне-Чирской (современное 
  название - Нижний Чир).<br>
  <br>
  Юрий Власов, петербургский исследователь истории 229-й дивизии (в ней воевал 
  и пропал без вести его дед - лейтенант госбезопасности Томилин, заместитель 
  начальника особого отдела 229-й), считает, что речь могла идти о понтонной переправе. 
  А железнодорожный мост на дороге от Суровикино в Сталинград (Волгоград) существует 
  и сегодня - он пересекает Цимлянское водохранилище, построенное после войны, 
  севернее, в районе станицы Чир.<br>
  <br>
  Юрий Власов прислал мне полученную в немецком Бундесархиве карту расположения 
  противоборствующих войск за 7 августа. Изучая с увеличительным стеклом в руках 
  саму местность, позиции полков 229-й и ее ближайших соседей, населенные пункты, 
  за которые сражались и к которым отступали, я пытаюсь представить себе, как 
  это все происходило в жаркие августовские дни в большой излучине, изрезанной 
  оврагами, по-местному - балками. Вот рядом со станицей Чир карандаш немецкого 
  офицера крупно вывел: «112 S.D.» Это ближайший левый сосед нашей дивизии, 112-я 
  стрелковая, сформированная в Омске полковником Сологубом.<br>
  <br>
  Позиции 112-й дивизии немцы прорвали 3 августа и заняли хутор Ново-Максимовский 
  на железной дороге. Штаб армии приказал 804 полку 229-й при поддержке соседей 
  слева и артиллерии вернуть хутор. Связь, как отмечено в созданной позднее лейтенантом 
  Черниковым истории соединения, была плохая. Когда полк подошел к Ново-Максимовскому, 
  ни бойцов из 112-й, ни артиллерии на назначенном для начала атаки месте не оказалось. 
  Командир СП-804 капитан Шаповаленко решил выполнить приказ самостоятельно. Без 
  поддержки артиллерией и пехотой. Ночная дерзость ему удалась, он отбил у немцев 
  хутор Ново-Максимовский. Наутро немцы вызвали артиллерию, танки и самолеты. 
  Полк Шаповаленко был вынужден отойти на прежние позиции.<br>
  <br>
  Судя по всему, это происходило уже 4-5 августа. Прорвав оборону и вынудив отступить 
  112-ю дивизию по железной дороге в сторону реки, немецкие танки и мотопехота 
  устремились вдоль Дона в северном направлении. С каждым часом отрезая от Дона, 
  от переправ и от возможности соединиться со своими и 229-ю дивизию полковника 
  Сажина, и 147-ю генерал-майора Вольхина. В районе хутора Пятиизбяный они вышли 
  к реке Дон. Дивизионный историограф 229-й лейтенант Черников с горечью констатирует: 
  «… дивизия оказалась в тылу у немцев, и роль заслона, которую она исполняла, 
  оказалась ненужной. Тем более, что немцы прорвали фронт и на правом фланге дивизии. 
  6 августа она была в кольце, которое сжималось с каждым днем».<br>
  <br>
  До трагического финала в большой излучине Дона оставалось всего четыре дня. 
  Потрепанные, потерявшие до половины списочного состава дивизии, с тревогой встречали 
  утро 7 августа. В 229-й, которая две недели подряд сражалась на главном направлении 
  немецкого удара, оставалось, по данным на 5 августа, всего-навсего пять с половиной 
  тысяч человек. Меньше половины.<br>
  <br>
  Через брешь, которую танковый удар гитлеровцев пробил в боевых порядках 112-й 
  дивизии, в тыл частям 62-й армии вышли три пехотные и одна танковая дивизия 
  вермахта. 112-я оказалась прижатой к Дону и попыталась выйти к железнодорожному 
  мосту у станицы Чир. Немцы стали обстреливать мост, через который уже переправлялись 
  на восточный берег реки отступающие войска. Здесь во время обстрела погиб комдив 
  112-й полковник Сологуб. 8 августа войска продолжали отступать на восток по 
  уже горящему мосту. В полдень немецкие танки прорвались к мосту, наши саперы 
  8 августа в 14.00 начали его взрывать. В половине третьего мост рухнул.<br>
  <br>
  В этот же день танковые клещи сомкнулись и окончательно окружили дивизии, оставшиеся 
  полностью или частично на западном берегу Дона. Приблизительное число оказавшихся 
  в окружении составило, по данным штаба Сталинградского фронта, 28 тысяч человек.<br>
  <br>
