  <span class="header"><strong>ПЕСНИ СВОБОДЫ</strong><br>
  Сегодня – 100 лет со дня рождения Мусы Джалиля<br></span>
  <br>
  <em>«Тюменская область сегодня», №26 (1646), 15 февраля 2006.</em><br>
  <br>
  <br>
  Имя этого автора в советское время было известно всем. Судьба воина и поэта, 
  волею судьбы оказавшегося в фашистском плену, но не сломленного обстоятельствами, 
  внушала уважение даже тем, кто далек от поэзии. Джалиль писал стихи, зная о 
  вынесенном ему смертном приговоре, писал, не надеясь, что их кто-нибудь увидит. 
  Его поэзия стала известна всему миру под названием «Моабитских тетрадей». <br>
  <br>
  Поэт (настоящее имя Муса Мустафович Залилов) родился в селе Мустафино бывшей 
  Оренбургской губернии. Уже тринадцатилетним мальчиком он пишет вполне профессиональные 
  стихи в духе восточной классики, увлекается Омаром Хайямом, Саади, Фазули, наизусть 
  знает Тукая. Муса учился в Оренбургском медресе «Хусаиния», которое после революции 
  было преобразовано в Татарский институт народного образования. В его подростковой 
  поэзии сочетались мотивы классики и гражданской войны, цитаты из Карла Маркса, 
  совсем детские строки о коршуне или цветах. Но уже в тридцатых годах Джалиль 
  завоевал в татарской поэзии прочное место: он был известен несколькими своими 
  стихотворными сборниками, прекрасной оперой «Алтынчеч», созданной совместно 
  с композитором Назибом Жигановым, поэмой «Письменосец», мастерски выполненной 
  в лучших традициях национального фольклора.<br>
  <br>
  Во время Великой Отечественной войны Муса Залилов служил корреспондентом армейской 
  газеты «Отвага» 2-й ударной армии Волховского фронта в звании старшего политрука. 
  В плен он попал раненным, предположительно 26 июня 1942 года, когда пробивался 
  из окружения на Волховском фронте. Затем были лагеря для военнопленных под Ригой, 
  в Демблине, в Вустрау, а к гестаповцам Джалиль попал в августе 1943 года, обвиненный 
  в разложении восточных легионов германской армии, состоящих из татар, башкир, 
  народов других национальностей СССР. Его заключили в одиночную камеру берлинской 
  тюрьмы Моабит и 25 августа 1944 года казнили на гильотине в тюрьме Плётцензее 
  в Берлине.<br>
  <br>
  «Моабитские тетради» писались на пыльных дорогах от одного города к другому, 
  из одного лагеря в другой, в тюрьмах - в тягостном ожидании приговора суда. 
  Среди них и те стихи, что были написаны в течение шести месяцев, последовавших 
  после известия о предстоящей казни. Его записные книжки со стихами были сохранены 
  товарищем по заключению, бельгийским антифашистом Андре Тиммермансом, который 
  после войны передал их советскому консулу. Сборник моабитских стихов впервые 
  был издан на татарском языке в Казани в 1953 году. В 1955 году он был выпущен 
  издательством «Молодая гвардия» под названием «Героическая песня». В 1957 году 
  за цикл стихов «Моабитская тетрадь» Мусе Джалилю посмертно присуждена Ленинская 
  премия. Указом Президиума Верховного Совета СССР за исключительную стойкость 
  и мужество, проявленные в борьбе с немецко-фашистскими захватчиками, ему посмертно 
  присвоено звание Героя Советского Союза. <br>
  <br>
  Стихи великого татарского поэта переведены на многие языки мира, знают его и 
  в Германии. Эрих Мюллер в сопроводительном пояснении к стихам писал: «Какая 
  трагедия и какой позор, что люди, убившие поэта, человека, который как истинный 
  друг любил Гете и Гейне, Баха и Бетховена, — были немцами. Этим мы нанесли ущерб 
  чести своей нации...» Читая их, мы ни на минуту не забываем, что их написал 
  человек, над головою которого навис топор палача. Об ужасах фашистской неволи 
  написано немало, но никто не расскажет об этом так, как сами узники концлагерей 
  и тюрем, свидетели и жертвы кровавой трагедии. <br>
  <br>
  ...23 апреля 1945 года воины 79-го стрелкового корпуса Советской армии, наступавшие 
  в направлении Рейхстага, увидели высившееся впереди серое мрачное здание тюрьмы 
  Моабит. Когда они вошли в ее двор, там никого не было, и только ветер гонял 
  обрывки бумаг... На странице одной из книг кто-то нашел такую запись на русском 
  языке: «Я татарский поэт Муса Джалиль, заключенный в Моабитскую тюрьму как пленный, 
  которому предъявлены политические обвинения, и, наверное, скоро буду расстрелян. 
  Если кому-нибудь из русских попадется эта запись, пусть передадут: это привет 
  товарищам писателям в Москве...» Бойцы выполнили эту просьбу.<br>
