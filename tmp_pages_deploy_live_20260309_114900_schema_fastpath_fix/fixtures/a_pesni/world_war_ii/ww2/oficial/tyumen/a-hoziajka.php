	<span class="header"><strong>ХОЗЯЙКА 
  БЕРЛИНА </strong><br></span>
<br>
  <em><strong>Подготовил Михаил Леонов</strong><br>
  <br>
&quot;Тюменские известия&quot;, №87-88 (4050-4051), 6 мая 2006 г.</em><br>
  <br>

<br>
  <img src="a-hoziajka.jpg" width="495" height="335"><br>
  <br>
  <strong>Фотография эта обошла весь мир: Америку, Англию, Германию, Испанию и 
  даже Новую Зеландию. Девушка-регулировщица в центре майского Берлина стала своеобразным 
  символом страны-победительницы.</strong><br>
  <br>
  Утро 2 мая 1945-го выдалось ласковым. Ефрейтор Шальнева регулировала движение 
  нашей военной техники в полутора километрах от рейхстага. Вдруг одна &quot;эмка&quot; 
  съехала на обочину, из машины вышел поэт Евгений Долматовский и фронтовой корреспондент 
  Евгений Халдей. Опытный глаз фотокора ТАСС сразу же &quot;выхватил типаж&quot;. 
  Халдей не вышел из машины спокойно, как это сделал. Долматовский, он выскочил 
  из нее, словно его ошпарили кипятком, чуть не сбив своего товарища с ног. <br>
  <br>
  Закружив вокруг девушки, будто шмель, он с улыбкой до ушей протараторил:<br>
  <br>
  — Скажи мне, красавица, откуда ты родом?! <br>
  <br>
  — Сибирячка я, из деревни, название которой вам ничегошеньки не скажет, — улыбнулась 
  в ответ регулировщица. <br>
  <br>
  Щелкнул затвор &quot;лейки&quot;, и Мария Шальнева попала в историю... <br>
  <br>
  ...Был конец тревожного 1941 года. Три девятнадцатилетние подружки из деревеньки 
  Передержка Омутинского района (этой деревни давным-давно уже нет) решили тайком 
  бежать из родного колхоза на фронт. &quot;Парни-сверстники наши вовсю воюют, 
  бьют ненавистных фрицев, а мы-то, что, хуже их?&quot; — рассудили девчата. <br>
  <br>
  О том, как попали они в действующую армию, Мария Тимофеевна описывает в своей 
  книжке &quot;Эх, дороги...&quot;, которая была издана в Воронеже уже в мирное 
  время (где наша героиня тогда и проживала). &quot;Сначала с горем пополам добрались 
  до Москвы: голодные, холодные и грязные. Где-то за Казанью начальник воинского 
  эшелона, сжалившись над нами, разрешил добраться до столицы в его поезде вместе 
  с бойцами, ехавшими на фронт. В подмосковном Перово эшелон разгрузили, там доформировывали 
  одну из дивизий, которую на третий день направили на оборону Москвы. Меня, Ию 
  Петрову и Надю Сурину, хотя и были мы без документов (не до них тогда было), 
  каким-то чудом определили в 87-й отдельный дорожно-эксплуатационный батальон. 
  А нам так мечталось попасть в санитарки или, на худой конец, в телефонистки, 
  и чтоб непременно на передовой. В дальнейшем этой &quot;передовой&quot; и горячих 
  фронтовых дорог хватило с лихвой&quot;.<br>
  <br>
  К весне 45-го за ее плечами остались тысячи километров, пройденных от Москвы 
  до Берлина. Две ее задушевные подружки, с коими бежала Шальнева из колхоза на 
  фронт, с коими питалась из одного солдатского котелка, навсегда остались в солдатских 
  могилах, даже неизвестно где. Таковы были суровые фронтовые дороги многих и 
  многих наших солдат... <br>
  <br>
  Ефрейтор Шальнева хозяйничала на улицах Берлина всего лишь 20 дней. Конечно 
  же, самым памятным из них оказался день 9 мая. <br>
  <br>
  &quot;Было раннее утро, и какая-то непривычная тишина, которая настораживала, 
  - пишет в своей книге Мария Тимофеевна. - Стою на постовом перекрестке и озадаченно 
  думаю: что бы это значило, в конце-то концов? И тут слышу, где-то в отдалении 
  захрипел громкоговоритель, потом четко объявляют: &quot;Победа!&quot; <br>
  <br>
  Со своим будущим мужем Мария познакомилась в послевоенном Берлине. Демобилизовавшись, 
  Мирон Шатунов увез свою возлюбленную к себе на родину - в Воронеж. Вместе с 
  мужем они воспитали двоих детей. Старшая Людмила работала конструктором на авиационном 
  заводе, сын Виктор - доктор наук, профессор. Марии Тимофеевне не суждено было 
  дожить до 60-летия Победы. Но ее жизнь продолжается в восьми внуках, которые 
  видят свою бабушку на страницах учебников истории. <br>
