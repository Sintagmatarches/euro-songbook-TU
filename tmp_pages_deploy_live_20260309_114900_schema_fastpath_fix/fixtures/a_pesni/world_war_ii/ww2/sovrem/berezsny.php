«Великая Отечественная»   (англ. The Unknown War - «Неизвестная война») — документальная эпопея (сериал) советско-британо-американского производства, вышедшая на киноэкраны в 1978 году. Рассказывает об участии СССР во Второй мировой войне и рассчитан на западного зрителя, для которого события на Восточном фронте были практически неизвестными.

Режиссер — советский кинодокументалист Роман Кармен. Сериал вышел в двух вариантах - для англоязычного и русскоязычного зрителя.<br>
