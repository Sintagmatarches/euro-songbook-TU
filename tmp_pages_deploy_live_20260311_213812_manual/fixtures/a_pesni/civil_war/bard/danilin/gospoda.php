<h1>ГОСПОДА ОФИЦЕРЫ...</h1>
Музыка и слова Виктора Данилина<br>
№3 из цикла "Белогвардейский фольклор"<br>
<br>
Господа офицеры, не грустите, не надо.<br>
Эта родина вам ничего не вернет.<br>
И дела не в погонах и не в царских наградах,<br>
Там теперь абсолютно незнакомый народ.<br>
<br>
Вспоминайте, как прежде вы любили и дрались,<br>
Вспоминайте друзей, вспоминайте врагов!<br>
Позабудьте, что вы без России остались,<br>
Доживайте свой век у чужих берегов!<br>
<br>
Пусть висят в старомодных багетах картины,<br>
Пусть звучит еле слышно старинный напев.<br>
Неужели самим вам порой не противно<br>
Наблюдать, как теперь жалок стал светский лев.<br>
<br>
Он в душе сохраняет звуки дивного бала,<br>
Он душой еще свеж, но здоровье не то.<br>
И тоска по тому, что в России осталось,<br>
Заставляет стонать перекошенным ртом.<br>
<br>
Пусть простится ему кровь защитников Пресни,<br>
Он раскаялся в том, но не сможет понять,<br>
Что в России теперь позабытые песни<br>
Вряд ли будут когда в полный голос звучать.<br>
<br>
Господа офицеры, вы любили и дрались,<br>
Вы хотели свое воротить хоть на миг.<br>
Позабудьте, что вы без отчизны остались<br>
И зажмите во рту умоляющий крик.<br>
