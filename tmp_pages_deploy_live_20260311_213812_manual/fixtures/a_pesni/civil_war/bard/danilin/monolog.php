<h1>МОНОЛОГ ЗА ЛОМБЕРНЫМ СТОЛИКОМ<br>
(ЗЕЛЕНОЕ НАД ЗЕЛЕНЫМ)</h1>
Музыка и слова Виктора Данилина<br>
Из цикла "Белогвардейский фольклор"<br>
<br>
Полковник, в железку сыграем давайте!<br>
Тасуйте колоду, на всех раздавайте!<br>
Чужое для нас продолжается время,<br>
И только седины свои во все темя,<br>
<br>
Корнет, Вы чудак, как на Вас погляжу я!<br>
Не надо подглядывать в карту чужую!<br>
Вы лихо ходили в атаку когда-то,<br>
Рубили с плеча большевистского брата.<br>
<br>
Достаточно крови мужицкой пролили,<br>
А сами детей на чужбине растили.<br>
Вот спросят они: &quot;Где же родина, папа?&quot;<br>
Не бойтесь,  корнет,  эти карты без крапа!<br>
<br>
А Вы, капитан, что дерете бородку,<br>
Берите икру, налегайте на водку!<br>
Опять загрустили по мхатовской &quot;Чайке&quot;?<br>
Хотите – сыграю Вам на балалайке.<br>
<br>
Опять на столе позабыли Вы карту,<br>
Не видно в Вас что-то былого азарту.<br>
А помню, в Крыму перед самым отходом<br>
Вы мне проиграли снарядов подводу!<br>
<br>
Полковник, Вы раньше честнее играли,<br>
И даже когда с перекопа удрали,<br>
Искали меня целый месяц в Стамбуле -<br>
Но все-таки долг до копейки вернули!<br>
<br>
Полковник. ей-богу, я Вас не узнаю:<br>
Как будто впервые я с Вами играю!<br>
Опять из кармана туза Вы достали -<br>
Уж лучше б чекисты тогда Вас забрали!<br>
<br>
Поручик, ну что Вы опять приуныли?<br>
В России и думать о нас позабыли.<br>
Я знаю: проигрывать Вам неохота,<br>
Но нужно и нам обыграть хоть кого-то!<br>
<br>
Поручик, оставьте опальные думы,<br>
Представьте себе, что под Вязьмой в саду мы!<br>
Взгляните - под окнами те же березы,<br>
Залейте вином ностальгию и слезы!<br>
<br>
Куда не посмотришь – печальные лица,<br>
Хотел бы я стать перелетною птицей,<br>
И в небо взлетев, повернувшись к востоку,<br>
Нестись вместе с ветром к России далекой!<br>
