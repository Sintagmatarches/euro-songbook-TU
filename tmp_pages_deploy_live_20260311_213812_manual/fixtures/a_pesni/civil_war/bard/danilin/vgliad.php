<h1>ВГЛЯДИТЕСЬ В ЛИЦО БЕЗ ГРИМА</h1>
Музыка и слова Виктора Данилина<br>
Из цикла "Белогвардейский фольклор"<br>
<br>
Вглядитесь в лицо без грима:<br>
Седые усы и брови.<br>
Гуляя пройдете мимо,<br>
А это корнет Дубровин.<br>
<div class='refren'>
А рядом, прижав ладошку<br>
К иссохшей груди, нелепо<br>
Умильно глядит на кошку<br>
Старик Генерал Кутепа.</div>
<br>
<span class='i'>Припев:<br></span>
 <br>
И штабс-капитан, и поручик,<br>
И даже корнет и полковник<br>
Смешались сегодня в кучу<br>
И стали друг другу ровня!<br>
<br>
Вот сыплет зерно на камни<br>
Уставшим от хлеба птицам<br>
Седой от тоски скитаний<br>
Поручик лихой Галицын.<br>
<div class='refren'>
Но, птиц разогнав клюкою,<br>
Суровый вам взгляд подарит<br>
С неизъяснимой тоскою<br>
Дряхлеющий граф Гагарин.</div>
<br>
<span class='i'>Припев.<br></span>
<br>
Давайте не будем строго<br>
Судить их былые страсти.<br>
Теперь под крылом у Бога<br>
Для них не найдется счастья.<br>
<div class='refren'>
Не будут в родных пенатах<br>
Для них волноваться рощи,<br>
Пусть сами они виноваты -<br>
Простить их, мне кажется проще!</div>
<br>
Простить и забыть до срока,<br>
Что Бог им в судьбе отметит.<br>
Не будем пенять без прока<br>
Пусть сами за все ответят.<br>
<div class='refren'>
Их мало уже осталось -<br>
Вояк под штандартом белым.<br>
Им мало тепла досталось,<br>
Такими их Бог уж сделал.</div>
<br>
<span class='i'>Припев.<br></span>
