        Я жила тогда у самой у реки, <br>
        Проходили мимо красные полки. <br>
        Враг проклятый их теснил со всех сторон, <br>
        На деревню к нам заехал эскадрон. <br>
        Эскадрон тогда разбили беляки, <br>
        И остался эскадронный командир. <br>
        Весь израненный во двор ко мне вбежал, <br>
        Знамя красное к груди своей прижал. <br>
        Лишь успела командира спрятать я, <br>
        Застучала, слышу, горница моя. <br>
        И в дверях раздался громкий крик: <br>
        «Отвечай, куда, где спрятан большевик?»<br>
        Отвечаю я: «А мне откуда знать?»<br>
        Офицер кричит: «Держать ее, пытать!»<br>
        Но пытали — допытаться не могли, <br>
        Постучали, покричали да ушли. <br>
        Как-то вышла посидеть я на крыльцо. <br>
        Вижу, полк ведет знакомое лицо. <br>
        Да ведь это мой знакомый командир! <br>
        Поравнявшися со мною, полк остановил. <br>
        С командиром сам Буденный был тогда. <br>
        Что, я думаю, случилась за беда? <br>
        Крикнул: «Смирно! Музыканты, в трубы дуть!»<br>
        И надел Буденный орден мне на грудь. <br>
