        И пусть создастся только фронт -<br>
        Пойдем врагов бить дружно. <br>
        <br>
        <em>
		Популярная комсомольская песня 20 — 30-х годов. Впервые 
        ее стали петь в 1922 г. в Киевских главных железнодорожных мастерских, 
        где в это время работал Н. Островский. По не установленным точно данным, 
        слова песни принадлежат комсомольцу, редактору стенгазеты Анатолию Красному 
        (Спиваку), а музыка – секретарю месткома, руководителю самодеятельного 
        струнного оркестра Павлу Зубакову (см.: «Рабочая газета», Киев, 17 июня 
        1940 г.). Как в записях, так и в публикациях 20-х годов песня варьируется. 
        См., например: Комсомольский песенник. Составлен ячейкой РЛКСМ. Изд. «Молодая 
        гвардия», М.-Л., 1925, стр. 72-73; песенник «Красное знамя». Предисл. 
        А. Луначарского. Изд. 2. М.-Л., 1931, стр. 50-51; Коробейник. Новый песенник. 
