        <br>
        1922<br>
        <br>
        <br>
        <img src="../oficial/parovoz.gif" width="596" height="762"><br>
        <br>
        <br>
        Вариант текста, знакомый по песенникам конца 
        XX века. Первоначально в первых строках упоминалась борьба не с белыми, 
        а Центральной радой - украинским национальным правительством, так как 
        песня сложена на Украине. Центральная рада возникла как объединение национальных 
        украинских партий и организаций в марте 1917 года. После прихода большевиков 
        к власти в Петрограде в октябре 1917 года провозгласила незвисимую Украинскую 
        народную республику. В январе (по новому стилю - феврале) 1918 года выбита 
        из Киева Красной гвардией. 1 марта 1918 вернулась в Киев вместе с германскими 
        оккупационными войсками, а 29 апреля ими же была разогнана.<br>
        <br>
        <br>
        <strong>Советская версия возникновения песни</strong> <br>
        <br>
        Впервые песня была исполнена на демонстрации 7 ноября 1922 года в Киеве; 
        сочинили ее молодые рабочие Киевских главных железнодорожных мастерских. 
        Достоверно авторы не установлены. Можно считать ее плодом коллективного 
        творчества. Разные источники автором текста указывают 
        комсомольца Бориса Скорбина либо редактора стенгазеты комсомольца Анатолия 
        Красного (Спивака), а автором музыкальной редакции - руководителя самодеятельного 
        оркестра Павла Зубакова (1891-1942). Мотив напоминает немецкую народную 
        песню «Аргонский лес в полночный час» - на Украину эта мелодия попала 
        в 1918 году во время оккупации, - а также популярный в то время вальс 
        В. Беккера &quot;Царица бала&quot;. <br>
        <br>
        <br>
        <strong>&quot;Антисоветская&quot; версия</strong><br>
        <br>
        &quot;Наш паровоз&quot; - переделка белогвардейской песни дроздовцев на слова Ивана Виноградова, посвященной генерал-лейтенанту Владимиру Константиновичу 
        Витковскому (1885-1978), начальнику 3-й (впоследствии Дроздовской) дивизии 
        с февраля 1919 года:<br>
        <br>
        <em>Чей черный «форд» летит вперед <br>
        пред славными полками <br>
        и кто к победе нас ведет <br>
        умелыми руками...</em><br>
        <br>
        См. <a href="../oficial/a-mihajlik.php">Елена Михайлик. &quot;Гренада&quot; 
        Михаила Светлова: откуда у хлопца испанская грусть?</a> (НЛО, 2005 №75).<br>
        <br>
        <br>
        В принципе, версии друг другу не противоречат. Просто советская не упоминает 
        о белогвардейской песне-предшественнице. Иван Виноградов - офицер-дроздовец, Гражданскую войну окончил в чине капитана, в эмиграции стал священником, арестован в Праге после ее освобождения в 1945 году, перевезен в СССР, отправлен в Карлаг,  в 1946 году выпущен по ходатайству Русской православной церкви, умер в чине архимандрита, настоятеля Вознесенского храма в г. Елец. Автор нескольких дроздовских песен (см. <a href="../bel/belglina.php">&quot;Полковник 
        Жебрак&quot; и &quot;Похоронный марш павшим при Белой Глине&quot;</a>, 
        обе на заимствованные популярные мелодии). А потому логично будет заключить, 
        что в основе действительно лежит белогвардейская песня, сочиненная дроздовцем 
        Виноградовым на популярный мотив (&quot;Аргонский лес в полночный час&quot; 
        или &quot;Царица бала&quot;). В 1922 году молодежь Киевских главных железнодорожных 
        мастерских переделала ее в &quot;Наш паровоз&quot;, дав песне новую жизнь. 
