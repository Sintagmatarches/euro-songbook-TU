        фольклор. Сост. В. М. Сидельников, под ред. Ю. М. Соколова. М., Изд. «Сов. 
        писатель», 1938, стр. 44-45; Песни Красной Армии. Воениздат. М., 1937, 
        стр. 212; Творчество народов СССР. Под ред. М. Горького, Л. З. Мехлиса, 
        А. И. Стецкого. Изд. редакции «Правды», 1937, стр. 274-275; Рукописный 
