 <br>
 <br>
  1-2 куплет Сергей Боханцев<br>
  3-4 куплет Шухрат Хусаинов <br>
 <br>
Как неслись тачанки полем на Воронеж,<br>
  Падали под пулями, как под косою рожь.<br>
  На тачанках сзади надпись &quot;Не догонишь!&quot;<br>
  Под дугою спереди: &quot;Живыми не уйдешь!&quot; <br>
 <br>
        <em>Припев: </em><br>
 <br>
Любо, братцы, любо. Любо, братцы, жить. <br>
  С нашим атаманом не приходится тужить. <br>
 <br>
Тело мое белое, сердце мое смелое<br>
  Вороны да коршуны на части расклюют.<br>
  Не горюй, мамаша, что взяла не наша,<br>
  Скоро ли догонят, да когда еще убьют? <br>
 <br>
Ало, братцы, ало кровь моя стекала,<br>
  Стремена за шпоры да руками по стерне...<br>
  Наискось рубашку расстегнула шашка,<br>
  Скоро конь буланый позабудет обо мне... <br>
 <br>
С немцами, японцами вслед за белой конницей<br>
  К западной границе потянулись облака.<br>
  Девица тоскует, солнце степь донскую <br>
  Красит в цвет рубахи молодого казака. <br>
