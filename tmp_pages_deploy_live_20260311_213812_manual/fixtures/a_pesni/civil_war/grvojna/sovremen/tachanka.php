          будет путь длинней – <br>
          мы успеваем <br>
          повернуть коней.<br>
          <br>
          В бешенной яри <br>
          мчат они вдогон. <br>
          Плечи сияют <br>
          золотом погон.<br>
          <br>
          Эх, ты, погоня – <br>
          гибель для глупца, <br>
          верные кони, <br>
          пули из свинца!<br>
          <br>
          Пыль слепит-душит, <br>
          грохот на всю степь, <br>
          рвутся к небу души <br>
          из пробитых тел!<br>
          <br>
          Слышишь, козаче, <br>
          повертай коня! <br>
          Что же ты скачешь <br>
          прямо на меня?<br>
