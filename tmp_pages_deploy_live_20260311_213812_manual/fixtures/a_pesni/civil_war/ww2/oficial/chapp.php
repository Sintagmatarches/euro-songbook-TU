  <br>
  Русский текст <a href="../../guerracivil/tschapajev.php">песни батальона &quot;Чапаев&quot;</a>  сформированного 11 ноября 1936 года в Альбасете и  включенного в XIII Интербригаду. Написана бойцом батальона немцем-студентом Вальтером Ульрихом. После битвы у Брунете (июль 1937 года) остатки батальона были распределены по другим батальонам интербригад. Песня впервые исполнена батальоном вечером 26 декабря в Альфамбре при отправке на фронт под Теруэль, где утром 27 декабря батальон принял первый бой. В боях ща Теруэль Вальтер Ульрих погиб.<br>
	<br>
