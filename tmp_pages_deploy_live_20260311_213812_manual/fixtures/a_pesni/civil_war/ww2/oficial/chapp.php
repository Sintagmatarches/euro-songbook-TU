	"Чапаев". Батальон двадцати одной национальности: Рассказы бойцов батальона / Сост. Альфред Канторович.  Пер. с нем. Риты Райт. М.: Худож. лит., 1939. С. 36:<br>
	</strong><br>
 &quot;Вечером 26 декабря бригада выступила. Батальон построился на площади Альфамбры. Командир бригады генерал Гомес, тогдашний командир батальона Клаус и тогдашний политкомиссар Отто Брунннер обратились к нам с речами. Ночи в горах Арагона холодные. Но в тот час все забыли об этом. Все слушали речи, и весь батальон запел «Чапаевскую песню»:
<br><br>
Все планы Франко<br>
Мы впрах разрушим,<br>
Наш вождь — Чапаев-партизан,<br>
Несем свободу<br>
На дулах ружей.<br>
Но пасаран, но пасаран!<br>
<br>
Автор песни, Вальтер Ульрих Фукс, немецкий студент, стоял тут же в наших рядах. В первый и в последний раз слышал он свою песню в устах всего батальона. Несколько дней спустя он погиб при исполнении обязанностей связиста, на боевом посту, - незабываемая жертва в борьбе за свободу.<br><br>
Двадцать седьмого декабря с семи часов утра начался артиллерийский обстрел фашистских позиций&quot;. <br>
