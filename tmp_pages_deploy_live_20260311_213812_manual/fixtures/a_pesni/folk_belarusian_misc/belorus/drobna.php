  За вячэрай баранская моладзь паказала й сваё майстэрства. Яны больш месяца рыхтаваліся 
  да сустрэчы. Спявалі розныя беларускія песні, у тым ліку і &quot;Дробна драбніца&quot;. 
  Гэта песня склалася ў Баранях і мела прататыпа – шайкунскую паню, якая слыла 
  скупасцю і папракала баранскую &quot;галоту&quot;, што жывуць бедна, а весяляцца, 
  &quot;мёд, гарэлку п'юць&quot;. Змест песні зацікавіў П. Мядзёлку і яна запісала 
  яе тэкст, а потым перадала Р.Шырму. Песня стала гучаць не толькі ў Беларусі, 
  а трапіла ў Пецярбург і Рыгу. Трапіла ў вялікі свет і другая песня з Барань 
  – &quot;Ідзём-пойдзем удоль вуліцы&quot;, якую перадаў Р.Шырму А.Стаповіч. Што 
  датычыць першай песні, то словы першапачатковага варыянта &quot;скупая пани&quot; 
  заменены на &quot;дзядзька багаты&quot;. Можа, да ўкладання песні прычыніўся 
  Альбін Стаповіч і не хацеў, каб яўны намёк на шайкунскую паню распаўсюджваўся 
  паўсюдна, таму замяніў словы: як-ніяк жылі Стаповічы са Свольненамі па-суседску 
  і былі ў добрых адносінах.<br>
