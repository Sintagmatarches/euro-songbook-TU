        <br>
        <span class="header"><strong>ГЕНОЦИД СЕМИРЕЧЕНСКОГО КАЗАЧЕСТВА</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 236-240</em>.<br>
  <br>
  <br>
        Вместе с начавшейся в 1917 г. новой Великой Смутой в Русской державе берет 
        свое начало и геноцид казачества, который с разной интенсивностью продолжался 
        все 74 года существования тоталитарного режима. В ходе осуществления его 
        выделяются два периода, во время которых уничтожение казачества происходило 
        наиболее активно. Первый и самый страшный период казачьего геноцида пришелся 
        на время братоубийственной Гражданской войны, когда из 4 млн казачьего 
        населения России, свыше 2 млн. было уничтожено физически. Многие тысячи, 
        спасаясь от верной гибели, были вынуждены покинуть Родину и навсегда уйти 
        в эмиграцию. Второй период геноцида казачества пришелся на годы проведения 
        всеобщей коллективизации 1929-1933 гг. от которой из всех сельских жителей 
        страны, более всего пострадали казаки. Духовное же уничтожение казачества 
        проводилось в течении всего времени существования антинародного режима, 
        вплоть до начавшегося в 1989 г. возрождения казачества. Частью геноцида 
        российского казачества явилось систематическое и планомерное уничтожение 
        семиреченского казачества. Все те ужасы, что обрушились на казачество, 
        в полной мере пришлось пережить и семиреченских казакам — физическое истребление, 
        расказачивание, насильственные мобилизации, уничтожение самобытного казачьего 
        уклада жизни, самоуправления, раскулачивание, насильственное переселение 
        с земель предков. <br>
  <br>
  Начало той страшной трагедии было положено событиями весны 1918 г. Но предпосылки 
  ее сложились еще раньше, со времени уничтожения в феврале 1917 г. русской исторической 
  государственности. Февральский переворот логически завершился переворотом октябрьским, 
  в результате которого к власти в стране пришло большевицкое руководство. Если 
  февральский переворот, спустя некоторое время большинство семиреченских казаков 
  все же признало, то октябрьский переворот, за исключением небольшой кучки отщепенцев, 
  признан не был. После октябрьского переворота, на территории Семиреченской области 
  власть в свои руки взяло семиреченское казачество в лице созданного им 1 ноября 
  1917 г. Войскового правительства, встав на пути распространения в Семиреченском 
  крае большевизма<sup>1</sup>. Однако в деле борьбы с врагами законной власти 
  Войсковое правительство руководствовалось больше выжидательной политикой и половинчатыми 
  мерами. Этим воспользовались большевицкие элементы развернувшие против власти 
  Семиреченского казачьего войска усиливавшуюся с каждым днем деятельность. Печальным 
  итогом всего этого явилась трагическая развязка, потянувшая за собой цепь кровавых 
  событий. В конце января 1918 г. в город Верный, столицу Семиреченского казачьего 
  войска (ныне г. Алма-Ата) из Ирана прибыл 2 Семиреченский казачий полк, распропагандированный 
  еще на фронте. Будучи уже в областном городе казаки второго полка, окончательно 
  подпали под влияние большевиков. Молодые казаки, не имевшие еще достаточного 
  жизненного опыта, легко поверили щедрым посулам большевиков, обещавших неприкосновенность 
  казачьих земель, сохранение казачьего уклада жизни, представительство в новых 
  органах, власти и т.д. 2 марта 1918 г. казаки 2 полка под руководством большевиков 
  подняв мятеж, совершили в городе Верном переворот, свергнув власть Войскового 
  правительства<sup>2</sup>. В результате этого, в Семиреченской области, как 
  и по всей России, установилась власть большевиков. Казаки, сами еще того не 
  понимая, привели к власти своих будущих палачей. Основные бедствия разразившейся 
  вскоре после переворота Гражданской войны в Семиречье, обрушились на расположенные 
  в Северном Семиречье Лепсинский и Копальский уезды, где в течении двух лет происходили 
  главные боевые действия. В станицах этих двух уездов и располагался 2 Семиреченский 
  казачий полк, казаки которого большей частью погибли в огне, гражданской войны, 
  заплатив своей кровью за совершенную в марте 1918 г. роковую ошибку. Захватив 
  власть, большевики сразу же заявили, что за совершенное в прошлом в отношении 
  них противостояние они никого не будут преследовать. Но это был всего лишь обычный 
  для новой власти подлый обман, которым она повсеместно и постоянно пользовалась. 
  Красные кому угодно давали любые обещания и шли на любые уступки, о которых 
  они забывали сразу же как только в них отпадала необходимость. Это громкое заявление 
  было сделано только с одной целью, выиграть время и укрепив свою власть в Семиречье, 
  разгромить казачество. В свою очередь, большая часть казаков не имела никаких 
  иллюзий в отношении ближайших намерений власти, и готовилась к отпору. Война 
  в крае началась со вспыхнувшего 16 апреля в Верненском уезде восстания Семиреченского 
  казачества. Толчком к той страшной трагедии послужили следующие события. В Верном 
  в это время ощущалась нехватка хлеба, вызванная произошедшим в Семиречье в 1917 
  г. неурожаем. Большевицкая власть решила выйти из положения, отобрав хлеб у 
  тех, кто его производил. После мартовского переворота власть в Семиреченской 
  области в связи с отсутствием в ней рабочих перешла в руки крестьян. Грабить 
  самих себя крестьяне естественно не собирались. Поэтому выход из продовольственного 
  кризиса был найден простой (по их мнению) — отобрать хлеб у казаков. Для выполнения 
  этого решения в Верном был сформирован продотряд и направлен в станицу Софийскую, 
  от казаков которой грабители потребовали сдать 1000 пудов хлеба и всё имеющееся 
  у них оружие. После этого, для устрашения они обстреляли станицу из двух пушек. 
  Попытка реквизиций вызвала среди казаков станицы взрыв возмущения, переросшего 
  затем в восстание. К повстанцам присоединились казаки ближайших пяти станиц. 
  Совместными действиями они разгромили продотряд и осадили г. Верный, что послужило 
  началом апрельского восстания<sup>3</sup>. <br>
  <br>
  Против повстанцев из Ташкента, был послан хорошо вооружённый Ташкентский экспедиционный 
  карательный отряд, под А. Мураева. Несмотря на героическое, упорное сопротивление, 
  восставшие казаки не смогли противостоять более сильному противнику, и были 
  вынуждены уйти в Китай и Северное Семиречье<sup>4</sup>. Взяв верх, карательный 
  отряд стал чинить в станицах Верненского уезда неслыханные зверства. Казачье 
  население было подвергнуто повальным грабежам, насилию и убийствам. Дома многих 
  казаков-повстанцев бандиты отряда Мураева сожгли. Казакам запретили даже называть 
  себя казаками. Отныне они должны были зваться только гражданами. Тех же, кто 
  осмеливался назвать себя казаками, расстреливали на месте. Расстрелу подлежали 
  также и все казаки, у которых каратели находили оружие. Захватив после упорного 
  боя Мало-Алматинскую станицу, отряд Мураева взял в плен более сотни казаков, 
  которые затем все были расстреляны. Затем каратели выгнали всё оставшееся после 
  ухода казаков население на её окраину в район Развилки (ныне — район Алма-Аты), 
  после чего их поставили на колени и навели пулемёты, продержав их, таким образом, 
  несколько часов. Разъезжая на лошади вокруг них, Мураев, осыпая грязной бранью, 
  угрожал всех их расстрелять, если казаки-повстанцы, избежавшие захвата в плен, 
  не явятся к нему и не сдадутся. Однако осуществить это злодеяние ему помешал 
  уполномоченный, посланный новой областной властью, после беседы с которым Мураев 
  отменил планируемый кровавый акт. <br>
  <br>
  В честь взятия» Мало-Алматинской станицы Мураев устроил для отряда грандиозное 
  застолье, в ходе которого станица подверглась повальному грабежу, насилию и 
  убийствам. Несколько казаков станицы, поддержавших большевицкую власть, Мураев, 
  дабы избежать их гибели в ходе погрома, посадил на сутки на гауптвахту. Каратели 
  убивали казаков, несмотря на их принадлежность к большевикам, только за то, 
  что они по происхождению казаки<sup>5</sup>. <br>
  <br>
  В станице Надеждинской мураевцы на глазах у жителей на центральной площади казнили 
  несколько десятков захваченных в плен казаков. Их поставили на колени, после 
  чего отрубили шашкой головы. Исполнять казнь поставили подростка, который из-за 
  своей физической слабости отрубал головы только после нескольких ударов, подвергая 
  обречённых казаков страшным мучениям. Просьбы приговоренных казаков назначить 
  для казни взрослого палача были проигнорированы. Жён и детей казаков, участвовавших 
  в восстании, мураевцы сажали в каменные подвалы домов, заколачивая выход гвоздями 
  и обрекая людей на долгую и мучительную смерть от жажды и голода. В результате 
  пострадали, в основном, женщины, старики и дети, ибо сами казаки ушли за пределы 
  уезда. Многие казаки, предвидя, что ждёт их семьи, уходя в Китай, забирали их 
  с собой. Количество казаков, погибших от рук мураевских изуверов, до сих пор 
  остаётся неизвестным. Составить представление об этом можно по одному достоверно 
  известному факту. Такие же насилия, как и в станицах, банда Мураева творила 
  в уйгурских селениях, мстя их жителям за поддержку апрельского восстания казаков. 
  По оценкам уйгурских историков, карателями было убито около семи тысяч уйгуров. 
  Количество же погибшего казачьего населения никто не считал. <br>
  <br>
  Апрельское восстание потерпело поражение, но в деле борьбы семиреченского казачества 
  с большевизмом оно имело огромное значение. Главным его результатом явилось 
  прекращение раскола семиреченских казаков на белых и красных. Казаки, которые 
  после октябрьского переворота стали на путь поддержки большевицкого режима, 
  или проявляли колебание после жестокого подавления апрельского восстания, увидев 
  истинную сущность новой власти, встали в ряды белых. Апрельское восстание послужило 
  началом второго этапа Белого движения в Семиречье. Если первый его этап в Семиреченском 
  крае был пассивно-оборонительным, явившимся реакцией на насильственный захват 
  большевиками власти в центре страны, желанием сохранить, что возможно от прежней 
  России, то второй этап Белого движения стал активным, повстанческим, явившимся 
  ответом на проводимую большевиками антинародную политику. <br>
  <br>
  Устроив кровавый погром южных станиц, большевики перешла к планомерному проведению 
  политики геноцида семиреченского казачества. Следующим актом геноцида явилось 
  начало проведения расказачивания. В июне 1918 г. было принято два соответствующих 
  этой политике решения. 3 июня командующим войсками красных Семиреченской области 
  был издан приказ о ликвидации Семиреченского казачьего войска: «Войсковое правление 
  и все станичные правления Семиреченского казачьего войска упраздняются. Приказываю 
  всё имущество, дела и денежные суммы бывшего Войскового правления, немедленно 
  сдать в распоряжение моё с военной коллегией. Для расформирования и ликвидации 
  всего казачьего управления учреждаю при штабе войск особое отделение»<sup>6</sup>. 
  6 июня Семиреченским облисполкомом было вынесено постановление о конфискации 
  у казачьих офицеров земли и сельхозинвентаря, а также о реквизиции у казаков 
  хлеба и скота<sup>7</sup>. Вскоре после этого стали проводиться переименования 
  станиц, выселков и превращения их в волости и села. Начавшись в Верненском уезде, 
  Гражданская война вскоре перекинулась в расположенные в Северном Семиречье Копальский 
  и Лепсинский уезды, где она затянулась на целых два года. Главной причиной войны 
  явилось острое неприятие казаками установившейся на их земле власти, несшей 
  людям одни только страдания и смерть. В этом кровавом противостоянии семиреченские 
  казаки боролись за право устраивать свою жизнь по обычаям своих предков, за 
  свою свободу, против жестокого насилия, за порядок, против произвола и анархии. 
  Началом Гражданской войны на севере области стало резкое обострение весной 1918 
  г. земельных конфликтов казаков с недавно переселившимися туда крестьянами. 
  Возмущение казаков вызывали претензии крестьян на их земли, в отношении которых 
  они хотели совершить передел в свою пользу. Не добившись от казаков согласия 
  на такой передел, крестьяне стали насильно захватывать их земельные наделы<sup>8</sup>. 
  Крестьяне Лепсинского и Копальского уездов (ныне территория Талды-Курганской 
  области Казахстана), пользуясь поддержкой уездных советов, где власть целиком 
  и полностью принадлежала им, стали устраивать в отношении казаков массовые насилия, 
  выражавшиеся кроме захвата их земель, в потравах их посевов, скашивании казачьих 
  лугов, угоне коней, нападениях на казаков и т.д., что вынуждало казаков принимать 
  ответные действия<sup>9</sup>. Результатом произвола явились начавшиеся кровавые 
  столкновения между казаками и крестьянами, переросшие затем в гражданское противостояние. 
  В июне 1918 г., сразу же после подавления апрельского восстания для разгрома 
  восставших станиц теперь уже на севере Семиречья из Верного был выслан крупный 
  карательный отряд И. Мамонтова. По прибытии на место к верненским карателям, 
  присоединилось большое количество местных крестьян. Вскоре из города Верного 
  в Северное Семиречье красными было выслано ещё два крупных карательных отряда. 
  Плохо вооружённые казаки, чьи станицы находились на большом расстоянии друг 
  от друга, не сумев противостоять более многочисленным и хорошо вооружённым отрядам 
  красных, были вынуждены прекратить сопротивление. Активные участники восстания 
  были вынуждены укрыться в горах Джунгарского Алатау или уйти на территорию Китая. 
  Только крепкие духом и сильные единством казаки станицы Саркандской сумели дать 
  достойный отпор многократно превосходящим их по численности красным. После подавления 
  восстаний, по северным станицам Семиречья прокатилась волна репрессий. Из трёх 
  действовавших на севере Семиречья красных отрядов особенно отличился в насилиях 
  над мирным казачьим населением отряд И. Мамонтова. Кроме этого, мамонтовцы повсеместно 
  проводили поголовное уничтожение станичных священников за то, что те благословляли 
  казаков на подвиг и жертву во имя победы над сатанинской властью. Ими же 16 
  сентября 1918 г. за городом Верным в роще Баума за проповеди, направленные против 
  новой власти, без суда и следствия был зверски убит священномученник епископ 
  Верненский и Семиреченский Пимен, ныне причисленный к лику местночтимых святых. 
  <br>
  <br>
  29 июля 1918 г. большевиками было издано постановление о конфискации у семей 
  казаков-повстанцев сельскохозяйственного инвентаря, что обрекало их на нищету 
  и голод<sup>10</sup>. В ноябре 1919 г. новой властью была проведена первая насильственная 
  мобилизация семиреченских казаков. Причиной этого явилось катастрофическое положение 
  большевиков в Семиречье в связи с разгромом мятежа крестьянских деревень с центром 
  в селении Черкасском, а также прибытием в Семиречье 33-тысячной Отдельной Оренбургской 
  армии А.И. Дутова. Возникла реальная возможность освобождения белыми всего Семиречья 
  от большевиков. В сложившейся ситуации красные, опасаясь восстания семиреченских 
  казаков у себя в тылу, срочно провели на территории Верненского уезда массовую 
  мобилизацию казачества. Мобилизованных сразу же отправили подальше от Семиречья 
  в г. Черняев (ныне — Чимкент), где из них был сформирован 1 Семиреченский казачий 
  полк, направленный ещё дальше от родных краев, в Ферганскую долину на борьбу 
  с басмачами. В Семиречье из Ташкента срочно были переброшены подкрепления. Все 
  красные части Семиречья были сведены в 3-ю Туркестанскую стрелковую дивизию. 
  В этой ситуации Советская власть решила временно изменить проводимую ею по отношению 
  к семиреченскому казачеству политику геноцида. В течение двух лет, пока шла 
  развязанная большевиками Гражданская война в Северном Семиречье, основными занятиями 
  находившихся там красных частей были не столько военные действия, сколько повальное 
  пьянство, грабежи и убийства безоружных жителей станиц. Факты мародерства, пьянства 
  и жестокого обращения с мирным казачьим населением были настолько вопиющими 
  и массовыми, что их в своих воспоминаниях были вынуждены признать даже те, кто 
  воевал в Семиречье в рядах красных. Ярким потверждением этого факта, является 
  характеристика красных войск Семиречья данная весной 1920 г. уполномоченным 
  фронта Туркестана Д. Фурмановым. В своём докладе РВС Туркфронта Фурманов сообщал 
  следующее: «Войска Семиречья состоя из местных жителей середняков и отчасти 
  из казаков, представляют собой весьма трусливую банду, зарекомендовавшую себя 
  в боях чрезвычайно гнусно. Красная армия Семиречья представляет собой не защитницу 
  Советской власти, а угрозу мусульманству и казачеству»<sup>11</sup>. Тут необходимо 
  учесть, что эта характеристика была дана в 1920 году, когда отряды красных в 
  Северном Семиречье уже были сведены в единое воинское формирование — 3-ю стрелковую 
  дивизию, с некоторой дисциплиной. Теперь исходя из всего вышеприведенного нетрудно 
  составить картину, что из себя представляли красные шайки в 1918-19 году, когда 
  в их рядах отсутствовали даже слабые намеки на дисциплину. Отход от политики 
  геноцида семиреченских казаков начался со времени принятия в декабре 1919 г. 
  командования 3-й Туркестанской стрелковой дивизии прибывшим из Ташкента Беловым, 
  бывшим до этого главкомом войск Туркестана. Он категорически запретил проводить 
  расстрелы взятых в плен семиреченских казаков. Вслед за этим Беловым был издан 
  еще один приказ, запрещавший насилия, грабежи и убийства в станицах: «...Все 
  зависит от вас или поможете прикончить фронт или подтолкнете казаков на дальнейшую 
  борьбу... Не насилуйте, не издевайтесь, не глумитесь...»<sup>12</sup>. Вскоре 
  после этого, 4 марта 1920 г., командующим Туркфронтом Фрунзе было издано воззвание 
  «К Семиреченскому казачеству и таранчинскому народу», в котором отмечалось, 
  что всем участвовавшим в боевых действиях против Советской власти в Семиречье, 
  если они добровольно сложат оружие, объявляется полная амнистия: «Уже два года 
  длится ожесточённая гражданская война на территории Семиречья. Сожженные сёла, 
  станицы и аулы, разорение и обнищание населения, превращённый в кладбище, прежде 
  цветущий край — всё это явилось его результатом. Ныне настал момент положить 
  конец этой бессмысленной войне. В интересах скорейшего безболезненного решения 
  кровавого спора на полях Семиречья, в интересах полного примирения всех трудящихся 
  края без различия веры, языка и национальности, Реввоенсовет постановил: всем 
  казакам, таранчам, киргизам и прочим, сражающимся ныне против Красной Армии, 
  гарантируется полная личная безопасность, забвение всех преступлений, совершённых 
  против рабоче-крестьянской России, при условии немедленного изъявления покорности 
  Советской власти, безоговорочного признания, сдачи всех запасов оружия и военного 
  снаряжения»<sup>13</sup>. Кроме этого, большевицкая власть давала обещание, 
  что бывшие ранее нормой, насилия проводимые в отношении семиреченских казаков, 
  теперь уже больше никогда не повторятся. Поверив обещаниям Фрунзе, а также понимая, 
  что одним им после поражения основной части армии адмирала А.В. Колчака не выстоять, 
  части Отдельной Семиреченской армии Б.В. Анненкова в конце марта 1920 г. сложили 
  оружие. Часть южной группировки этой армии, состоявшей, в основном, из семиреченских 
  казаков под командованием войскового старшины Бойко оказалась окружённой в станице 
  Копальской, превосходящей её по численности группировкой красных. Семиреченские 
  казаки, имея продовольствия лишь на несколько дней, а боеприпасов только на 
  один бой, в виду безвыходности ситуации 29 марта 1920 г. сложили оружие. После 
  этого, сдавшиеся в плен казаки были заключены в расположенный в городе Верном 
  лагерь. Уже в лагере часть казаков была подвергнута арестам ЧК, имели место 
  случаи грабежа казаков лагерной охраной<sup>14</sup>.<br>
  <br>
  Первая фаза Гражданской войны в Семиречье, отличавшаяся широким масштабом военных 
  действий, закончилась. Печальным итогом ее явились опустевшие, разоренные и 
  сожженные станицы. Тысячи семиреченских казаков полегли на полях братоубийственной 
  войны или стали калеками. Многие, бросив имущество, были вынуждены эмигрировать 
  в Китай, где им пришлось остаться на долгие десятилетия. Часть казаков так и 
  не вернулась из эмиграции. Тысячи были подвергнуты массовым насильственным мобилизациям 
  и отправлены проливать свою кровь за чуждое им дело ненавистного режима. С поражением 
  Отдельной Семиреченской армии Анненкова, гражданское противостояние в крае не 
  закончилось. С лета 1920 г. по конец 1922 г. в Семиречье прошел второй этап 
  Гражданской войны. В отличие от первого он не сопровождался столь масштабными 
  боевыми операциями, но был не менее кровопролитным и ожесточенным. По характеру 
  военных действий, второй этап Гражданской войны в Семиреченском крае напоминал 
  прошедший в первой половине 1918 г. ее начальный повстанческий период. Результатом 
  трагических событий весны 1920 г. в Семиречье явился полный и окончательный 
  захват края большевиками. <br>
  <br>
  Несмотря на сложившуюся здесь для сопротивления крайне неблагоприятную ситуацию, 
  далеко не все белые сложили оружие. Часть семиреченского казачества во главе 
  с исполняющим обязанности Войскового атамана, генерал-майором Щербаковым, полная 
  решимости продолжить борьбу с большевицким режимом, ушла в западно-китайскую 
  провинцию Синьцзян и расположилась в находящемся неподалеку от границы городе 
  Кульджа. В Синьцзян со своими отрядами ушли атаманы Анненков и Дутов. Всего 
  в Западном Китае оказалось около 10 тысяч бывших белых, преимущественно казаков. 
  Оказавшись в эмиграции, семиреченские казаки сразу же возобновили активную вооруженную 
  борьбу с властью большевиков. Казаки совершали стремительные рейды на территорию 
  Советской России, громя органы власти и уничтожая отряды красных. После чего 
  также неожиданно исчезали, как и появлялись. В этой рейдовой войне особенно 
  отличился отряд под командованием полковника Сидорова, активно использовавший 
  эту тактику еще в 1918-1920 гг. Граница между Семиреченской областью и Западным 
  Китаем в то время напоминала линию фронта. В свою очередь, красные, стремясь 
  предотвратить нависшую со стороны ушедших за кордон казаков угрозу своему господству, 
  использовали в борьбе с ними все доступные средства. ЧК широко развернула среди 
  казаков агентурную сеть, значительно затруднившую их борьбу с большевицким режимом. 
  Кроме того, среди эмигрировавших казаков активно проводилась пропагандистская 
  кампания за возвращение. Казаков всячески уговаривали вернуться домой, обещая 
  забыть их участие в белом сопротивлении и не допускать проведения произвола 
  и насилия в отношении казачества<sup>15</sup>. Кампания эта имела лишь частный 
  успех, да и то, только лишь весной-летом 1920 г. Часть ушедших казаков, не выдержав 
  обрушившихся на чужбине на них многочисленных лишений, голода, тоскуя по родине 
  и своим близким, а также, поверив обещаниям, вернулась в Семиречье. Но все заверения 
  и в этот раз оказались обманом — большинство вернувшихся казаков, спустя некоторое 
  время, было расстреляно. Из эмиграции тогда вернулась только небольшая часть 
  семиреков. Когда до казаков в Синьцзяне дошли известия о репрессиях в отношении 
  казаков-репатриантов, поток возвращающихся быстро иссяк. В противостоянии с 
  укрывшимися в Синьцзяне казаками новый режим широко использовал власти этой 
  китайской провинции. Большевики использовали подкуп продажных властей Синьцзяна, 
  а в случае несговорчивости, предъявляли им ультимативные требования, подкрепленные 
  угрозами военного вторжения на территорию этой провинции<sup>16</sup>. Используя 
  подобные методы воздействия большевики неоднократно добивались разрешения на 
  ввод в эту провинцию крупных карательных отрядов, совершивших в период с 1921 
  по 1924 гг. несколько рейдов по расположенным там казачьим поселениям<sup>17</sup>. 
  <br>
  <br>
  После того как весной 1920 г. на всей территории Семиреченской области был установлен 
  тоталитарный режим, начались волнения крестьян-переселенцев, вызванные распространением 
  продразверстки на переселенческие села Семиречья. Недовольство усилил изданный 
  командующим Туркфронта приказ об отправке на войну с басмачами в Ферганскую 
  долину не желавшей уходить из Семиречья 3-й Туркестанской стрелковой дивизии, 
  состоявшей преимущественно из этих же крестьян-переселенцев. Недовольство вылилось 
  в произошедшее в июне 1920 г. восстание пятитысячного гарнизона Верного<sup>18</sup>. 
  Незадолго до восстания большевицкие власти области, видя, что контроль за ситуацией 
  в городе уходит из их рук и, опасаясь возможного участия пленных казаков в назревавшем 
  вооружённом выступлении, в начале мая выпустили их из верненского лагеря. <br>
  <br>
  Из освобожденных семиреченских казаков, возраст которых был не старше 30 лет, 
  были сформированы и отправлены на борьбу с басмачеством в Ферганскую долину 
  кавалерийские части. Казаки, возраст которых был старше 30 лет, распускались 
  по станицам. Однако в формируемые кавалерийские части в качестве добровольцев 
  записывались и многие казаки старших возрастов из опасения перед расправой со 
  стороны большевицкого режима. Отправка семиреченских казаков на Ферганский фронт 
  была сделана с целью ослабить их, отослав возможно большее количество казаков 
  самых боеспособных возрастов подальше от родных мест. Насильственные мобилизации 
  и посылка семиреченских казаков в Фергану проводились и впоследствии в течение 
  всего периода активной войны с басмачами в Средней Азии, вплоть до ликвидации 
  Ферганского фронта летом 1926 г. Стремясь забрать на фронт как можно больше 
  семиреков, власть отправила воевать даже 16-летних казачат. <br>
  <br>
  Весной 1920 г. многим семирекам казалось, что новая власть наконец-то оставит 
  казаков в покое. Однако с окончанием братоубийственной войны на казаков обрушились 
  новые беды. Проводимый в их отношении геноцид не только не прекратился, но даже 
  усилился. Разоружив Семиреченских казаков и ослабив их массовыми мобилизациями, 
  новая власть провела следующий этап расказачивания семиреков. <br>
  <br>
  Из-за того, что в Северном Семиречье сломить казачье сопротивление красным удалось 
  лишь в конце марта 1920 г., в апреле того же года было издано еще одно постановление 
  о ликвидации Семиреченского казачьего войска, идентичное приказу об уничтожении 
  войска от 2 июня 1918 г.<sup>19</sup> Были продолжены переименования станиц 
  и выселков, повсеместно уничтожались памятники, связанные с историей и культурой 
  семиреченского казачества. <br>
