        <strong>Донесение штаба Уральской Отдельной армии от 22.10.1918 г. </strong><br>
        <br>
        Борьба за Родину. Разгром большевиков на севере Уральского фронта. <br>
        <br>
        Наступление большевиков производилось тремя группами с целью охвата севернее 
        Уральска на Таловую; на Уральск и южнее Уральска на Скворкин. Нашими полками 
        наголову разбита Таловская группа, состоявшая из дивизии Чапаева, причем 
        один полк уничтожен совершенно, остатки другого полка бежали. Взяты 2 
        орудия, 730 винтовок, 2 аэроплана, весь обоз с боевыми припасами и пленные. 
        Наши полки преследуют остатки Чапаевской дивизии. <br>
        <br>
        Центральная группа, в составе дивизии, также основательно потрепанная, 
        поспешно отступает. Наши полки ее окружают. Захвачен грузовой автомобиль 
        с патронами и винтовками, огромный обоз с боевыми припасами и пленные. 
        Третья группа у Скворкинской станицы окружается нашими полками. Отступление 
        ее невозможно. Настроение в наших войсках — решительное и твердое. Все 
        уверены в скором разгоне неприятельских сил. <br>
        <br>
        Фомичев. <br>
        <br>
        <em>ГА РФ. Ф. Р-127. Оп. 1. Д. 32. Л. 101. </em><br>
        <br>
        <br>
        <strong>Из воспоминаний сотника Б. Кирова</strong><br>
        <br>
        От полковника Кириллова я получил приказание идти к поселку Щапово, куда 
        уже отправилась 1-я сотня 3-го учебного полка и занять его. Мы направились 
        туда на рысях. Скоро мы увидели цепь 1-й сотни и я послал туда казака 
        передать ее командиру, что я с сотней прибыл, вступаю в его подчинение 
        и жду приказаний. <br>
        <br>
        Поселок был хорошо укреплен и занят большими силами красных. Расположен 
        он был на более высоком месте чем та позиция, где мы находились, и поэтому 
        степь около него уже успела просохнуть и подойти к нему можно было со 
        всех сторон. Но в то же время, сделать это было очень трудно, так как 
        окружавшая его степь была совершенно ровной, и в силу этого все наступающие 
        части могли свободно обстреливаться с закрытых позиций поселка. <br>
        <br>
        Взвесив все выгоды и невыгоды нашего положения, равно как и положение 
        красных, я с командиром сотни 3-го полка есаулом Б. Железновым решили, 
        что нам силами двух сотен взять поселок невозможно и поэтому мы только 
        ограничились действиями, целью которых было выяснить положение и силы 
        противника. Рассыпавшись лавой, мы одной редкой цепочкой начали наступать 
        на поселок. Красные, видя, что нас мало, подпустили лаву очень близко 
        к поселку и потом открыли по нам огонь из пулеметов и двух орудий. Огонь 
        был несильным, и мы продолжали двигаться вперед. Временами нам начинало 
        казаться, что красных в поселке немного и что нам удастся его занять. 
        Но огонь постепенно усиливался и в конце концов мы принуждены были отойти, 
        не понеся, правда, потерь. Эта демонстрация показала нам, что силы противника 
        значительны, но мы все же не могли о них составить более точного представления. 
        Через некоторое время мы повторили прежний свой маневр, но на этот раз 
        уже наступление повели энергичнее, надеясь заставить красных показать 
        все свои силы. При первых выстрелах мы перешли на рысь, а затем на намет, 
        дав понять противнику, что мы решили атаковать поселок. Маневр удался 
        и красные, настроенные нервно во все дни своего весеннего отступления, 
        показали действительно все свои силы. По нам они били минимум из 6 орудий, 
        и пулеметные пули по-настоящему засыпали наши сотни. <br>
        <br>
        Мы быстро отошли. Странно, потерь у нас не было совершенно. Может быть, 
        красные волновались и стреляли, куда попало, а может быть, ровная, как 
        море, степь обманывала глаз и не давала возможности правильно определить 
        прицел. Но еще вернее, что испарения просыхающей степи, преломляя лучи 
        солнца, обманывали зрение, создавая мираж. Кто был в степях, тот знает, 
        что миражи бывают не только в пустыне Сахара. Они встречаются на каждом 
        шагу и у нас в степях. Очень часто они принимают настолько реальные формы, 
        что даже старые казаки, знавшие степь с детства, часто ошибаются. Миражи 
        эти бывают иногда удивительно красивы, что уже невозможно ошибиться, что 
        это мираж. Иногда вам же кажется, что впереди вас в нескольких верстах, 
        а иногда всего в саженях 200—300 расположен поселок, около него — гумна, 
        вода и около нее — плотина, обсаженная деревьями, ходят верблюды и так 
        далее и так далее. Вы удивлены, так как знаете, что здесь нет ничего подобного, 
        но картина эта настолько ясная, что невольно вы начинаете сомневаться 
        и думать, что забыли, что здесь есть жилье. Часто в жаркий тихий летний 
        день, когда высохшая степь мучает вас жаждой и горячее солнце палит вас 
        своими лучами, вы, поднимаясь на сырт, видите такой хуторок и около него 
        воду. Молодые казаки, еще не привыкшие таким фокусам и обманам степи, 
        бросаются за водой. Удержать их на месте невозможно. Они не верят тому, 
        что это только кажется, и мчатся с чайниками за водой, но, проскакав 100—300 
        саженей, смущенные, возвращаются обратно. Такие же миражи часто обманывают 
        и во время боя. Достаточно маленького течения воздуха, насыщенного испарениями, 
        между вашим расположением и расположением противника, чтобы никакие, даже 
        самые точные приборы не смогут определить расстояние между вами. Думаю, 
        что так было и в этот раз. Как бы то ни было, мы отошли, не понеся потерь, 
        но для нас вполне было ясно, что если бы мы даже сумели дойти до окопов 
        противника, сумели бы проскочить их и ворваться е поселок, то там нам 
        был бы конец, настолько наши силы не соответствовали силам красных. <br>
        <br>
        Отойдя, мы решили ждать подхода наших частей. Ждать долго не пришлось. 
        Со стороны нижней линии подходила наша 1-я дивизия, а со стороны хуторов 
        и линии железной дороги — 2-я. Обе дивизии, не подходя близко к поселку, 
        остановились и развернулись в боевой порядок на расстоянии верст трех 
        от него. Мы ждали большого боя и думали, что обе дивизии ринутся на поселок 
        и уничтожат всю группировку красных, засевшую в нем. Наши ординарцы поскакали 
        с донесениями, точно рисующими положение. Через некоторое время нами был 
        получен приказ: наступать на поселок. Думая, что за нами пойдут части 
        дивизий, мы начали наступление, но остались одни и, встреченные еще более 
        сильным огнем, понеся потери, должны были отойти. <br>
        <br>
        Полки и сотни не двигались нам на помощь. На все наши просьбы о подкреплении 
        нам отвечали неизменным приказом наступать и взять поселок. <br>
        <br>
        К полудню к нам подошла полусотня 33-го Николаевского полка*, которая 
        сообщила нам, что на фронт прибыл атаман, который нам и приказал взять 
        поселок. <br>
        <br>
        Мы опять двинулись вперед, но были опять отбиты и, неся потери, отошли. 
        Чем руководствовалось наше начальство, я сказать не могу, но, несмотря 
        на наши донесения и просьбы прислать нам помощь, мы получали неизменное 
        приказание наступать и взять поселок. Помощи же не было. В этот день мы 
        сделали 6 атак на окопы противника, но безрезультатно. К вечеру нервы 
        уже притупились, когда мы получили приказание уже от самого атамана, которое 
        гласило: «Наступать или расстреляю». Мы снова пошли в атаку. Казаки знали 
        приказ. Мы спешились, оставили коноводов на открытом месте, и пошли на 
        поселок. Красные, уже привыкшие к нашим атакам за день, не обращали на 
        нас большого внимания и всю силу своего артиллерийского огня перенесли 
        на расположение наших двух дивизий. Это дало нам возможность подойти к 
        поселку почти вплотную, но все же мы ясно сознавали, что взять его нам 
        не удастся. Мы шли вперед, зная, что немногие вернутся назад от окопов 
        красных. Но, невзирая на это, настроение у всех было бодрое. Так бывает 
