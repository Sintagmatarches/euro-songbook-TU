        всех вперед и выпускал в красных очередь за очередью. Своей целью он избрал 
        батарею из двух орудий, стоящих за мельницей, хорошо видимых с нашего 
        расположения. Орудия эти отвечали на его очереди картечью. Это был поединок! 
