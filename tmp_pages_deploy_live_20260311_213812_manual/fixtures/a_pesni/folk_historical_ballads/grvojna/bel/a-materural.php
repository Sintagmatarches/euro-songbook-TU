	<br><span class="text"><span class="header"><strong>МАТЕРИАЛЫ 
        К ИСТОРИИ АНТИБОЛЬШЕВИЦКОГО ДВИЖЕНИЯ В УРАЛЬСКОМ КАЗАЧЬЕМ ВОЙСКЕ </strong><br></span>
        <br>
       Предисловие и публикация С.С. Балмасова<br>
        <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 167-170</em>.<br>
        <br>
        <em><br>
        Публикуемые документы из фондов ГА РФ, на мой взгляд, весьма ярко характеризуют 
        историю борьбы уральского казачества с большевиками. Первый документ - 
        отрывок из воспоминаний одного из самых знаменитых командиров Уральского 
        казачьего войска, Павла Андреевича Фаддеева «Эпизоды из боев на Уральском 
        фронте» интересен тем, что наглядно характеризует боевые качества ближайших 
        соседей уральских казаков — оренбуржцев и то, что уральцы не оставались 
        безучастными к борьбе против большевиков в других районах. Кроме того, 
        отрывок позволяет оценить их роль в подъеме на борьбу с коммунистами как 
        раз самих оренбургских казаков. <br>
        <br>
        В публикуемом под номером 2 донесении изложены подробности отражения очередного 
        наступления красных на Уральск. Документ позволяет делать выводы о том, 
        что «прославленный» красный комдив В.И. Чапаев был не раз бит уральскими 
        казаками. Документ 3 является фрагментом воспоминаний сотника Б. Кирова 
        о борьбе уральского казачества против Советской власти. Интересен он тем, 
        что позволяет еще раз убедиться в героизме уральских казаков, которые 
        неоднократно ходили в атаки против во много раз численно и технически 
        превосходящего врага. Он относится к апрелю 1919 г. <br>
        <br>
        Документ 4 хранится вместе с бумагами одного из лидеров Белого движения 
        на Восточном фронте генерала М.К. Дитерихса, к которому попал, очевидно, 
        от кого-то из ушедших на Дальний Восток уральских казаков. <br>
        <br>
        </em>
        <hr>
        <br>
        <strong>Из воспоминаний П.А. Фаддеева «Эпизоды из боев на Уральском фронте» 
        </strong><br>
        <br>
        Весной 1918 г. Уральское правительство находилось уже на положении войны 
        с Советами, и им велись переговоры с красным Саратовым. Оренбург и Илецкая 
        Защита были сданы красным частям после первого разгрома атамана Дутова, 
        который защищал город Оренбург с господами офицерами, юнкерами Оренбургского 
        военного казачьего училища, кадетами двух корпусов и совсем незначительной 
        частью казаков Оренбургского казачьего войска. Ровно на второй день Святой 
        Пасхи, утром, часов в 8, в Илецкой станице раздался набат. Все казаки 
        и офицеры, по положению, спешили к станичному правлению, чтобы узнать, 
        что это значит. Начальник района или командующий Илецким фронтом полковник 
        Д.А. Балалаев объявляет, что двум сотням молодых казаков, постановок в 
        казаки годов 11, 12, 13 и 14-го Уральским войсковым правительством приказывается 
        сегодня же выступить из города Илека и направиться на город Оренбург и 
        в станице Краснохолмской поступить в распоряжение полковника Нагаева. 
        Зачем это делается и для чего, как сказал полковник Балалаев, «узнаете 
        на месте, в станице Краснохолмской от полковника Нагаева». Командирами 
        этих сотен состояли я и подъесаул Я.Я. Сорокин. Младших офицеров было 
        по 2—3 человека в каждой сотне. По получению этого распоряжения начались, 
        как водится, сборы в поход. Выступление назначено в 12 часов, после молебна 
        на станичной площади. <br>
        <br>
        Молебен, на котором присутствовала буквально вся станица, был торжественно 
        отслужен, так как это выступление правильно организованными двумя сотнями 
        за пределы Илека было первым в эту войну. Кроме того, родственники и знакомые 
        десятками пришли поглазеть и проводить своих близких. Не без женских слез, 
        конечно, обошлись эти проводы! Но и песни казацкие, лихие, раздавались 
        из сотен, выходящих из станицы на поддержку казакам-соседям, оренбуржцам. 
        Никаких обозов, провианта, патроны — только в патронташах — брать с собой 
        мы не стали по приказу командования. Всем этим, по условию, должны были 
        нас снабжать и снабдили впоследствии оренбуржцы. <br>
        <br>
        Дорога от Илека до Оренбурга не была занята красными частями. Они занимали 
        тогда только города Оренбург и Илецкая Защита и охраняли линию железной 
        дороги Оренбург-Илецкая Защита, а потому мы без особых приключений доехали 
        до назначенной цели. Граница земель двух войск, Уральского и Оренбургского, 
        представляет некоторый интерес. Она проходит в 12 верстах от станции Илецкая 
        Защита. <br>
        <br>
        В наших сотнях было приблизительно по 150 всадников. Половина из них была 
        вооружена трехлинейными винтовками, по 20—30 патрон на каждую. Другая 
        половина из казаков была вооружена ружьями всех систем — берданы, «гра», 
        «ватерли», штуцера, вплоть до дробовых охотничьих ружей с разным количеством 
        патронов, не превышающим 30 штук. По 10-15 казаков в сотнях были вооружены 
        лишь пиками и шашками. <br>
        <br>
        В станице Краснохолмской мы торжественно были встречены восставшими казаками 
        во главе с полковником Нагаевым. У церкви была выстроена воинская часть; 
        обменивались почестями. Мы немедленно были размещены по квартирам с тем, 
        чтобы утром выступить по направлению к Оренбургу совместно с присоединившимися 
        к нам местными оренбургскими казаками. По выяснении обстановки оказалось, 
        что среди казаков Оренбургского войска в данном районе происходит некоторое 
        брожение: одни хотели непременно воевать против большевиков и освободить 
        Оренбург, другие, наоборот, выражали желание покориться им и «принять 
        советскую власть». Как те, так и другие, в конце концов, не были уверены 
        в правильности своих взглядов, и, главным образом, не были информированы 
        и не знали, что делалось в эти дни на белом свете, какую линию ведут все 
        остальные казачьи войска. Потому офицеры, стоявшие во главе сторонников 
        войны против Советов, нашим приходом хотели, как живым факелом, указать 
        колеблющимся, что их соседи, мы, уральские казаки, воюем против большевиков, 
        и им не следует от нас отставать и укрепить в этой мысли своих сторонников. 
        Доходило до того, что оренбургские казаки нарочно приходили к нашим и 
        спрашивали их: «Не юнкеря ли Вы?» «Не офицеры ли Вы, переодетые в казацкую 
        форму, подговоренные своим начальством, чтобы обмануть нас?» Но когда 
        они убеждались, что нет, когда слышали от казаков, что все наше войско 
        целиком против большевиков и остальные войска — тоже, то сомнения их разрушались, 
        и они поголовно все шли с нами. Таким образом, когда мы дошли до станицы 
        Павловской в 22 верстах от Оренбурга, то за нами уже тянулись нескончаемые 
        обозы с провиантом и до 10 тысяч присоединившихся к нам оренбургских казаков. 
        <br>
        <br>
        Уже в станице Дедюревской решено было на военном совете атаковать одновременно 
        Илецкую Защиту и Меновой Двор в 7 верстах от Оренбурга, после чего, соединившись, 
        общими усилиями взять Оренбург. Нашему Уральскому дивизиону было приказано 
        идти с частями, направленными для взятия Илецкой Защиты. Правда, ни Оренбурга, 
        ни даже Илецкой Защиты нам в это время взять не удалось. Взяты они были 
        месяц спустя, но факт второго восстания оренбургских казаков против советской 
        власти состоялся. После этого они уже не задумывались, а разом поделились: 
        большая часть — против большевиков, а меньшая — за них. <br>
        <br>
        Два раза под командованием полковника Нагаева мы подступали к Илецкой 
        Защите с целью овладения ей, но оба раза самые пустячные случаи помешали 
        нам даже вступить в бой с красными частями, с гарнизоном города. Первый 
        раз, мы, идущие ночью всем отрядом по направлению к Илецкой Защите, были 
        сбиты с толку тремя, вероятно случайными или преждевременными выстрелами 
        из пушек в нашем направлении и, как нам казалось, или, правильнее сказать, 
        вновь сформированным дружинам из оренбургских казаков, произведенными 
        в нашем направлении. Но факт налицо: после этих выстрелов ни одного дружинника-оренбургца 
        не осталось с нами — все повернули и поскакали по хуторам. Остался на 
        месте лишь наш дивизион. <br>
        <br>
        Было после этого нескольких дней сборов и длительных клятв — «драться 
        отчаянно». Потом мы снова двинулись на Илецкую Защиту, но уже с другой 
        стороны — с южной. Не доходя 7—8 верст до Илецкой Защиты, мы узнаем от 
        нашей разведки, что на последнем разъезде железной дороги Илецкая Защита 
        -Оренбург стоит бронированный поезд, 2 или 3 вагона с красны- ми частями, 
        в которых находятся 2 роты противника, которые не могли помешать нам. 
        Их было так мало для нас, что наш успех тут был совершенно обеспеченным. 
        Этот успех был нам необходим, чтобы поднять дух в наших войсках. А потому 
        мы, конечно, направились всеми силами, чтобы овладеть этим поездом. Но 
        каково же было наше разочарование, когда от первого пулеметного выстрела 
        частями из оренбургских дружин был повторен старый «маневр» — они все 
        в панике разбежались, оставив на месте нас и свои потерянные шапки, фуражки 
        и папахи. <br>
        <br>
        Не хочу этим фактом обвинять оренбургских казаков в трусости и объясняю 
        эти случаи тем, что их части или дружины были только что сформированы, 
        вернее, собраны, не было ни офицеров, ни вооружения, и не были еще выработаны 
        приемы войны в такой обстановке. Мы же, с грустью, потеряв всякую надежду 
        на взятие Илецкой Защиты, вернулись к исходному пункту, Илеку... <br>
        <br>
        <em>ГА РФ. Ф. 5881. Оп. 2. Д. 697. Л. 1-3. </em><br>
