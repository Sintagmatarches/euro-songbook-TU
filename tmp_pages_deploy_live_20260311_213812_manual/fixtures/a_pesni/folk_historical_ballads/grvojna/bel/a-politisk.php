	<span class="text"><strong>О.В. Ратушняк </strong><br>
  <br>
      <span class="header"><strong>ПОЛИТИЧЕСКИЕ ИСКАНИЯ ДОНСКОГО И 
        КУБАНСКОГО КАЗАЧЕСТВА В ГОДЫ ГРАЖДАНСКОЙ ВОЙНЫ В РОССИИ (1918-1920 гг.)</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 17-23</em>.<br>
  <br>
  <br>
  Гражданская война в России — одна из сложных, многозначных, внутренне противоречивых 
  тем отечественной историографии. Несмотря на то, что многие исследователи неоднократно 
  обращались к ней, в ее рамках осталось еще немало дискуссионных проблем. К проблемам 
  подобного рода относится выявление причин поражения Белого движения. По всей 
  видимости, одной из них являются противоречия внутри антибольшевицкого лагеря. 
  В немалой степени этому способствовало стремление казачества (по крайней мере, 
  его определенной политически активной части) найти свой особый путь развития. 
  Рассмотрение различных аспектов политических исканий донского и кубанского казачества 
  в годы Гражданской войны в России и является целью настоящей статьи. К определяющим 
  факторам данного процесса относятся: внутреннее положение казачьих краев и областей, 
  их взаимоотношения между собой и с другими участниками антибольшевицкого движения. 
  <br>
  <br>
  Казачество, являвшееся когда-то символом свободы и вольности, со временем стало 
  одним из сословий Российской империи. Революционные события 1917 г. не обошли 
  стороной и казачьи области. Как и Россия в целом, казачество оказалось на распутье. 
  Основным вопросом политической жизни стал вопрос выбора дальнейшего пути развития, 
  который, в конечном счете, привел к братоубийственной бойне. При этом революция 
  и Гражданская война не столько раскололи страну (как считают, некоторые исследователи), 
  сколько подчеркнули и выявили противоречия, существовавшие внутри российского 
  общества в целом и казачьих областей, как его составляющей. <br>
  <br>
  К факторам, оказавшим немаловажное влияние на ход и итоги Гражданской войны, 
  относятся, в частности, расслоение внутри казачества и его взаимоотношения с 
  иногородним населением казачьих районов. Одной из главных причин участия большинства 
  казаков в войне на стороне антибольшевицких сил было стремление сохранить свои 
  привилегии. Однако двойственность их положения заключалась в том, что, отстаивая 
  свои сословные привилегии, казаки боролись с такими пережитками феодализма, 
  как сословные повинности. По-видимому, это было одним из факторов того, что 
  первоначально основная масса казачества заняла нейтральное отношение к большевистской 
  власти. <br>
  <br>
  Особенно большое влияние на казаков оказало обещание большевиков прекратить 
  тяжелую, разорительную мировую войну, которая тяготила казачество, несшее основную 
  ее тяжесть. Поэтому приходившие с фронта казачьи части, поддерживавшие лозунг 
  большевиков о мире, не препятствовали установлению в казачьих областях Советской 
  власти. Наибольшей симпатией большевики пользовались среди беднейших слоев казачества. 
  Благожелательному отношению казаков к Советской власти способствовало и то, 
  что долгий отрыв от хозяйственной обстановки вследствие пребывания на фронте 
  в какой-то мере деклассировал часть казачества, притупив инстинкт мелкого собственника.<sup>1</sup> 
  Однако, сделав первоначально ставку на иногороднее население, большевицкое правительство 
  побудило его к активным выступлениям за передел земли. И здесь казачество, не 
  желавшее расстаться со своими сословными привилегиями, не могло остаться в стороне. 
  Даже Донской революционный комитет, избранный 23 января 1918 г. в станице Каменской, 
  и фактически поддержавший Советскую власть в ее борьбе против атамана А.М. Каледина, 
  не хотел, и слышать о переделе земли в пользу иногородних. Члены Комитета «весьма 
  недружелюбно отнеслись к притязаниям иногородних на общий раздел донской земли 
  и совсем не собирались кончать с общеказачьими привилегиями».<sup>2</sup><br>
  <br>
  Такое же отношение к земельному вопросу было и на Кубани. Здесь краевое правительство, 
  стремясь не обострять отношения с иногородними и беднейшей частью казачества, 
  всячески затягивало решение аграрной проблемы. Иногородняя часть населения первое 
  время мирилась с таким положением. Так, на съезде уполномоченных от населенных 
  пунктов Кубанской области фракция иногородних приняла обращение к Кубанской 
  Раде и войсковым частям, в котором доводило до их сведения, что «никаких притязаний 
  к казачьим паевым землям и к казачьему войсковому имуществу предъявлять не намеревается, 
  будучи убеждено, что Учредительное собрание найдет возможность удовлетворить 
  насущные нужды иногороднего населения области, не нарушая интересов трудового 
  казачества».<sup>3</sup> Однако, пропагандистская деятельность большевиков, 
  меньшевиков, эсеров и октябрьские события 1917 г. способствовали активизации 
  иногороднего населения. В декабре 1917 г. 2-й общеобластной съезд представителей 
  казаков, иногородних и горцев Кубани принял решение об отмене посаженной платы. 
  В феврале 1918 г. под нажимом беднейшего казачества и части иногороднего населения 
  Кубанская Краевая Рада вынуждена была издать «Проект правил об урегулировании 
  земельных и сельскохозяйственных отношений в кубанской области». Но это уже 
  не могло предотвратить выступлений иногородних. По всей области начались самовольные 
  захваты и переделы земли. Исполком Советов Кубанской области объявил Кубанскую 
  Краевую Раду и Кубанское правительство вне закона. Части 39-й пехотной дивизии 
  и отряды местных революционных сил развернули наступление, целью которого было 
  свержение кубанского правительства. Несмотря на разворачивающиеся события, казачество 
  в большей своей массе занимало выжидательную позицию. Это подтверждается теми 
  фактами, что оно допустило разгон малого Войскового Круга в Новочеркасске, изгнание 
  Кубанской Краевой Рады и правительства из Екатеринодара, а также занятие своих 
  территорий советскими войсками, сопротивление которым оказывали в основном части 
  зарождающейся Добровольческой армии. <br>
  <br>
  Истоки нейтралитета казачества довольно точно выявляет в своих воспоминаниях 
  А.И. Деникин: «Определилось яснее настроение донских казаков. Не понимают совершенно 
  ни большевизма, ни «корниловщины». С нашими разъяснениями соглашаются, но как 
  будто плохо верят. Сыты, богаты и, по-видимому, хотели бы извлечь пользу и из 
  «белого», и из «красного» движения. Обе идеологии теперь еще чужды казакам, 
  и больше всего они боятся ввязываться в междоусобную распрю».<sup>4</sup> Это 
  высказывание верно и в отношении кубанского казачества. <br>
  <br>
  Отношение казаков к советской власти в конце 1917 — начале 1918 г. зафиксировано 
  в воспоминаниях многих очевидцев тех событий, в частности А.П. Богаевского: 
  «Отравленные пропагандой на фронте, строевые казаки спокойно ждали Советской 
  власти, искренно или нет считая, что это и есть настоящая народная власть, которая 
  им, простым людям, ничего дурного не сделает. А что она уничтожит прежнее начальство 
  — атамана, генералов, офицеров да, кстати, и помещиков, так и черт с ними... 
  Вообще настроение всего казачества в массе мало чем отличалось от общего настроения 
  российского крестьянства: казаки еще не испытали на своей шее всей прелести 
  советского управления... Остальные «держали нейтралитет»».<sup>5</sup><br>
  <br>
  Несмотря на пламенные речи генерала Л.Г. Корнилова, казачество не вступало в 
  ряды Добровольческой армии. Не менее равнодушно отнеслось оно и к призывам вступить 
  в кубанскую армию, создававшуюся кубанским краевым правительством. <br>
  <br>
  Политика нейтралитета, свойственная основной массе казаков в первые годы гражданской 
  войны, явилась, по мнению А.А. Зайцева, выражением определенной психологии фронтового 
  казачества. Казаки-фронтовики не желали выступать с оружием в руках ни против 
  «своих» зажиточных станичников, ни против иногородних, с которыми их связывали 
  узы фронтового братства.<sup>6</sup><br>
  <br>
  Уставшее от войны казачество, частично пропитанное к тому же духом социал-демократических 
  и эсеровских идей, не поддержало Добровольческую армию. Во время «Ледяного» 
  похода казаки, в большинстве своем, встречали добровольцев «либо безразлично, 
  либо враждебно».<sup>7</sup><br>
  <br>
  За период с весны до осени 1918 г. на Дону и Кубани произошел переход от поддержки 
  Советской власти беднейшим казачеством, в том числе казаками-фронтовиками, при 
  нейтралитете основной массы середняков, к выступлению против большевиков большинства 
  казачьего населения. Этому перелому в настроении донского и кубанского казачества 
  способствовал ряд факторов. <br>
  <br>
  Во-первых, в казаке победил инстинкт собственника над инстинктом труженика, 
  чему в немалой степени способствовала земельная политика советских органов власти 
  в казачьих районах. На первом этапе аграрных преобразований (весна 1918 г.) 
  Советы наделяли крестьянские и казачьи бедняцкие хозяйства земельными участками 
  за счет конфискации церковных, монастырских, крупных частновладельческих земель. 
  Эту конфискацию использовало и кулачество, стремившееся присвоить себе помещичьи 
  земли, инвентарь и скот. Однако когда встал вопрос о судьбе войсковых земель, 
  перестройке сословного землепользования казачества и уравнивании его с остальной 
  массой сельского населения, кулацкая часть казачества открыто выступила против 
  Советской власти. Впоследствии ее поддержали середняки и часть казачьей бедноты. 
  Как отметил С.М. Буденный, «некоторые казаки, как только речь заходила о наделе 
  иногородних землей, говорили: «Мы не против Советов, а земельку нашу не трожь 
  не вами дадена.»».<sup>8</sup><br>
  <br>
  Во-вторых, классовая политика, проводившаяся большевиками на первом этапе гражданской 
  войны, способствовала разжигания сословной розни. Иногородними, пользуясь одобрением 
  и поддержкой советской власти, стремились решить свои проблемы (в первую очередь, 
  земельный вопрос) за счет казачества, а заодно и свести счеты с последним. Это 
  привело к росту погромов казаков, расстрелам и грабежам со стороны иногороднего 
  населения. По свидетельству А.И. Деникина, «большинство иногородних принимало 
  то или иное, хотя бы и косвенное участие в обездолении казачества».<sup>9</sup><br>
  <br>
  В-третьих, среди украинских партизан, отступивших под натиском немцев в казачьи 
  районы, получили большое распространение грабежи казачьих станиц под лозунгом 
  борьбы с контрреволюцией. Мародерство было распространено и среди некоторых 
  красноармейских отрядов, состоявших из иногородних. Все это, а также акты «расказачивания», 
  носившие в 1918 г. стихийный характер реквизиций в виде изъятия оружия, лошадей, 
  сбруи и снаряжения, проводившиеся в оскорбительной для казачества форме, привело 
  к тому, что середняцкая часть казачества поддалась на агитацию казачьих верхов 
  и открыто выступила против советской власти. «Унижаемые морально, разоряемые 
  материала но и истребляемые физически, кубанские казаки (и не только кубанские 
  - О.Р.) скоро стряхнули с себя всякий налет большевизма и начали подниматься».<sup>10</sup><br>
  <br>
  Все перечисленные выше причины перелома в настроении казачества были замечены 
  и впоследствии учтены большевиками. Так, Г.К. Орджоникидзе отмечал, что «при 
  первых же попытках проведения земельной реформы казачество стало во враждебную 
  позицию по отношению к Советской власти... С другой стороны, отступающие украинские 
  войска продолжали свои бесчинства и грабежи в Кубанской области. Много было 
  неумелых, в высшей степени нетактичных поступков со стороны отдельных местных 
  работников, находившихся у власти».<sup>11</sup><br>
  <br>
  Однако, несмотря на изменение отношения к большевикам, взаимоотношения казаков 
  с командованием Добровольческой армии по-прежнему были сложны и противоречивы. 
  Основным узлом противоречий являлось, с одной стороны, желание части казаков 
  сохранить независимость от России (причем, как от большевицкой, так и антибольшевицкой), 
  с другой стороны — стремление руководства Добровольческой армии подчинить себе 
  казачьи силы, как в военном, так и в социально-экономическом и политическом 
  вопросах. Масло в огонь подливало и то, что в добровольцах казаки видели, прежде 
  всего, представителей и защитников отжившей свое российской монархии. В то же 
  время в Добровольческой армии «создалось если не враждебное, то, во всяком случае, 
  недоброжелательное отношение к многостепенной кубанской власти, слишком напоминавшей 
  ненавистный офицерству «совдеп» и слишком резко отмежевавшейся от общерусской 
  идеи».<sup>12</sup><br>
  <br>
  Уже на первой встрече между добровольцами и командующим войсками Кубанской области 
  генералом В.Л. Покровским в ауле Шенджий в середине марта 1918 г. произошла 
  стычка. Первые ясно показывали свое стремление подчинить себе кубанцев. Соединение 
  Добровольческой армии с казаками, по мнению руководства добровольцев, могло 
  произойти лишь на двух основных условиях: упразднение Кубанского правительства 
  и Рады (в этом проявилось нежелание белых генералов признать за Кубанью право 
  на государственную самостоятельность) и подчинение атамана Кубанского казачьего 
  войска командующему Добровольческой армии. <br>
  <br>
  17 марта 1918 г. на совещании представителей Кубани с командованием Добровольческой 
  армией в станице Новодмитриевской было подписано соглашение, по которому кубанский 
  правительственный отряд переходил в полное подчинение генералу Л.Г. Корнилову, 
  а командующий войсками Кубанского края с его начальником штаба отзывались в 
  состав Кубанского правительства для формирования Кубанской армии. Согласно данному 
  соглашению Законодательная Рада, Войсковое правительство и атаман должны были 
  продолжать свою деятельность, содействуя военным мероприятиям командующего Добровольческой 
  армии. Как вспоминал генерал А.П. Богаевский «в этом союзе не было взаимного 
  доверия и искренности. Только суровая необходимость заставила обе стороны сойтись».<sup>13</sup> 
  Дальнейшие события полностью это подтвердили. Строки, касающиеся формирования 
  Кубанской армии «введенные по настоянию кубанских представителей, — по словам 
  А.И. Деникина, — главным образом якобы только для морального удовлетворения 
  смещенного командующего войсками создали впоследствии большие осложнения во 
  взаимоотношениях между главным командованием и Кубанью».<sup>14</sup><br>
  <br>
  Стремление игнорировать кубанскую власть и утвердить свою проявилось и в назначении 
  генерала Деникина накануне первого апрельского штурма Екатеринодара генерал-губернатором 
  Кубанской области. <br>
  <br>
  В дальнейшем, с занятием добровольческими частями большей части территории Кубани, 
  политика командования Добровольческой армии по отношению к кубанцам становится 
  все более непреклонной и жесткой. Это отложило отпечаток и на взаимоотношения 
  казаков-кубанцев с добровольцами. Так, в одном из документов того периода говорится, 
  что взгляд на Добровольческую армию как на освободительную «начинает тускнеть 
  под влиянием незаконных действий многих частей и лиц армии, которые ведут себя 
  в республике как в покоренной стране».<sup>15</sup> Замена красного террора 
  белым и монархические лозунги, вырывавшиеся из рядов добровольцев, так же, как 
  и лозунги «единой и неделимой» постепенно отталкивали часть казаков от деникинской 
  армии. Немало этому способствовали и местные самостийники своей пропагандой. 
  Однако во время Второго Кубанского похода разногласия между кубанскими органами 
  власти и А.И. Деникиным еще не коснулись рядового казачества. По словам Д.В. 
  Леховича: «Оно шло за своими офицерами. А кубанские офицеры — воспитанники русских 
  военных училищ — смотрели на события глазами русского офицера. Они с недоверием 
  относились к деятельности своего правительства, а многие из них готовы были 
  без церемоний расправиться с самостийными вожаками. И те это отлично знали».<sup>16</sup><br>
  <br>
  Одной из причин разногласий между Добровольческой армией и казаками (особенно 
  донцами) была ориентация последних на Германию как возможного союзника в борьбе 
  с большевиками. Командование же Добровольческой армии придерживалось прежней 
  ориентации на союзников по Антанте. Помимо задачи освобождения России от большевиков 
  лидеры Белого движения преследовали цель сохранить целостность российской территории. 
  С этих позиций генерал Деникин и его окружение считали, что кроме борьбы с большевиками 
  необходимо вести борьбу с Германией и пресекать любые самостийные попытки отделиться 
  от России. В то же время донцы в лице правительства и атамана считали своей 
  основной задачей освобождение территории Дона от большевиков и в этой борьбе 
  они готовы были принять любую поддержку. Не связанные непосредственным обязательством 
  перед Англией и Францией они воспринимали Германию как силу, способствовавшую 
  их целям и задачам. Здесь вполне можно согласиться с А.А. Гордеевым, который 
  отмечал, что появление в пределах границ Области Войска Донского германских 
  вооруженных сил воспринимались казаками и атаманом как определенная поддержка 
  союзников в борьбе с большевиками.<sup>17</sup> Хотя, конечно, Германия воспринималась 
  донцами как вынужденный союзник, выбранный по принципу наименьшего зла. Подтверждение 
  этому мы находим в приказе донского атамана П.Н. Краснова, отданном 4 мая 1918 
  г.: «Дон считается с фактом занятия части территории германскими войсками, смотрит 
  на них не как на врагов, но как на союзников в борьбе с большевиками и старается 
  использовать их для вооружения и снабжения всеми средствами борьбы своей армии».<sup>18</sup><br>
  <br>
  Антантовская ориентация генерала Деникина и его окружения и германофильские 
  воззрения П.Н. Краснова откладывали свой отпечаток не только на взаимоотношения 
  между ними, но и на стратегические планы в ходе Гражданской войны. Одной из 
  причин июньского похода на Кубань Добровольческой армии было нежелание действовать 
  совместно с донскими казаками, к этому времени открыто принявшими германскую 
  ориентацию. <br>
  <br>
  В то же время руководство Добровольческой армии понимало необходимость союза 
  с казачеством. В день открытия Кубанской Рады в Екатеринодаре 1 ноября 1918 
  г. генерал Деникин, призывая к единению, заявил о том, что «добровольческая 
  армия признает необходимость и теперь, и в будущем самой широкой автономии составных 
  частей русского государства и крайне бережного отношения к вековому укладу казачьего 
  быта».<sup>19</sup> Несмотря на это заявление, многие противники А.И. Деникина 
  считали его лозунг о «непредрешенчестве», т.е. о непредрешении форм правления 
  в России и о месте казачьих краев в составе России, обычной политической уловкой. 
  В их глазах Деникин всегда оставался сторонником «единой и неделимой» России 
  и защитником монархического строя. По мнению П.Н. Краснова, «генерал Деникин 
  не имел ничего на своем знамени кроме лозунга единой и неделимой России».<sup>20</sup><br>
  <br>
  Однако, несмотря на острые разногласия, в конце декабря 1918 г. состоялось официальное 
  признание генерала А.И. Деникина главнокомандующим Вооруженными силами на Юге 
  России (ВСЮР) и подчинение ему Донской армии. Среди причин принятия подобного 
  решения можно выделить в частности революцию в Германии, повлекшую за собой 
  вывод оккупационных немецких войск с территории казачьих краев, вызвавший ослабление 
  донских и кубанских самостийников приближением большевистского фронта к границам 
  Дона и усиление позиций Англии и Франции, а, следовательно, и Деникина. <br>
  <br>
  Немаловажное значение во взаимоотношениях добровольцев с казаками играл тот 
  фактор, что Добровольческая армия формировалась на казачьей территории. Здесь 
  же располагались ее основные базы. Не случайно, что после освобождения казачьих 
  районов от власти большевиков отношения между руководством Добровольческой армией 
  и казачьими политическими лидерами обострились еще больше. Ряд казачьих политических 
  деятелей желали играть еще более важную роль при решении, как военных, так и 
  политических вопросов. Однако их взгляды, отражающие по преимуществу местные 
  интересы, очень часто шли вразрез с мнением главного командования. В частности, 
  освобождение казачьих территорий из-под большевицкой власти последнее считало 
  только частью стратегического плана по освобождению всей территории России. 
  Представители же донского и кубанского казачества в большинстве своем полагали, 
  что с освобождением казачьих районов борьбу с Советской властью можно считать, 
  в основном, законченной. Как верно заметил Р. Медведев в своей книге «Октябрьская 
  революция»: «...сбросив Советскую власть у себя дома, казаки не имели ни какого 
  желания идти дальше и завоевывать Россию для белых генералов».<sup>21</sup> 
  Это вызывало резкое недовольство со стороны добровольцев. Довольно сильно обостряло 
  отношение и то обстоятельство, что, встречая противодействие со стороны донских 
  и, особенно, кубанских властей, А.И. Деникину постоянно приходилось вмешиваться 
  во внутренние дела казачьих областей, что, в свою очередь, вызывало недовольство 
  казачьих органов власти. Все это создавало напряженную атмосферу взаимного недоброжелательства, 
  которая не могла не отразиться на боевом духе и состоянии антибольшевицких войск. 
  <br>
  <br>
  Исходя из того, что на Кубани самостийные тенденции имели больший вес, чем на 
  Дону, взаимоотношение здесь с командованием ВСЮР было более острым и напряженным. 
  Не случайно А.И. Деникин не хотел создания Кубанской армии, хотя кубанцы постоянно 
  и настойчиво этого добивались, ссылаясь на такой прецедент, как Донская армия 
  и обещания добровольческого командования во время Первого Кубанского похода. 
  Он понимал, что создание такой армии только даст лишний козырь в руки местных 
  самостийников, а, учитывая тяготение некоторых из них к Украине, превратит их 
  в опасного противника в тылу добровольцев. <br>
  <br>
  В мае 1919 г. в связи со сменой кубанского правительства отношения между Кубанью 
  и командованием ВСЮР обострились. В это время проденикинское правительство Ф.С. 
  Сушкова сменил кабинет П.И. Курганского, выражавший интересы черноморской части 
  казачества.<br>
  <br>
  Особенно обострились отношения кубанцев с деникинцами в связи с отправкой делегации 
  представителей Кубани на Парижскую мирную конференцию. Основной задачей делегации 
  было добиться признания, если не государственной самостоятельности, то, по крайней 
  мере, автономии Кубани. <br>
  <br>
  5 февраля 1919 г. кубанская и донская делегации совместно с представителями 
  Украины и Белоруссии обратились в Одессе к Верховному командованию держав Антанты 
  с меморандумом. В нем предлагался проект создании России на федеративных началах 
  путем «добровольного соглашения, как равные с равными, тех государственных образований, 
  которые выкристаллизовались на развалинах старой России». Под этими «государственными 
  образованиями» понимались, в первую очередь, территории Дона, Кубани, Украины 
  и Белоруссии. Стремясь избавиться от опеки главного командования ВСЮР, представители 
  Дона и Кубани просили Верховное командование держав Антанты оказывать им материальную 
  и, прежде всего, военную помощь напрямую. С этой же целью в меморандуме проводилась 
  мысль о необходимости создания на казачьих территориях самостоятельных армий 
  и образовании общего оперативного штаба с державами Согласия «без вмешательства 
  его в политические и внутренние дела названных государственных образований».<sup>22</sup><br>
  <br>
  В Париже кубанцами проводилась та же идея, но уже с четким требованием признать 
  Кубань «самостоятельным государственным образованием». При этом подчеркивалось, 
  что «кубанские казаки не желают входить в состав Советской России, как они не 
  хотят быть и в России царистской и вообще монархической».<sup>23</sup><br>
  <br>
  Фактически деятельность кубанской делегации в Париже свелась, в основном, к 
  двум меморандумам и частным беседам (например, с американским президентом В. 
  Вильсоном). Несмотря на то, что кубанская делегация на мирную конференцию допущена 
  не была, выступления ее членов и поданные меморандумы отложили свой отпечаток 
  на последующие взаимоотношения между главнокомандующим ВСЮР и частью членов 
  Кубанской Рады. Особенно обострил эти отношения договор, заключенный в июле 
  1919 г. кубанской парижской делегацией с представителями меджлиса Горской республики. 
  По этому договору части Кубанской армии в случае нахождения на территории республики 
  должны были в оперативном отношении подчиняться ее военному командованию. Как 
  мы увидим в дальнейшем, факт заключения договора кубанцев с горцами, направленный 
  фактически против командования ВСЮР, оказался позднее на руку последнему. <br>
  <br>
  После убийства председателя рады М.С. Рябовола 27 июня 1919 г., выступившего 
  в первый день работы Южно-русской конференции с антиденикинской речью, радой 
  была открыто провозглашена необходимость борьбы не только с Красной армией, 
  но и с монархизмом, процветавшим в армии генерала Деникина. «К началу осени 
  1919 г. многие депутаты Рады вели энергичную пропаганду за отделение своей области 
  от России и не стесняясь бранили деникинское правительство. Они всячески подрывали 
  авторитет Кубанского атамана, называя его ставленником Деникина, и удаляли из 
  высшего управления краем всех казаков, сочувствовавших идеям Добровольческой 
  армии. И уже в виде открытого вызова белому командованию вели переговоры с Грузией 
  и Петлюрой... Положение становилось чрезвычайно напряженным, так как пропаганда, 
  направленная против армии и ее командования, постепенно начинала проникать в 
  ряды кубанского казачества на фронте».<sup>24</sup><br>
  <br>
  Наиболее ярыми самостийниками являлась черноморская часть кубанского казачества, 
  среди которой были особенно сильно развиты проукраинские настроения. По мнению 
  генерала Деникина, слово «черноморец» стало синонимом украинофила и сепаратиста. 
  Линейные казаки, опасавшиеся национальной дискриминации, ожидавшей их в случае 
  украинизации Кубани, выступали против украинофильской тенденции черноморцев. 
  Это противостояние внутри кубанских институтов власти мешало руководству ВСЮР 
  проводить свою линию, как в военном, так и в политическом отношениях. Обострившаяся 
  обстановка на фронте, а также усилившаяся антиденикинская пропаганда со стороны 
  черноморской части Рады, вынудили генерала Деникина пойти на решительные меры 
  в вопросе отношений с кубанцами. И здесь, как нельзя кстати пригодился договор, 
  заключенный кубанской парижской делегацией с представителями Горской республики. 
  Последняя находилась в состоянии войны с Терским казачьим войском, которому 
  покровительствовало командование ВСЮР. Таким образом, договор, заключенный между 
  Кубанью и Горской республикой, мог рассматриваться как направленный против главного 
  командования ВСЮР. На этом основании 7 ноября 1919 г. генерал Деникин отдает 
  приказ о немедленном предании полевому суду всех лиц, подписавших этот договор.<sup>25</sup> 
  Один из участников парижской делегации Ф.И. Кулабухов был повешен. Остальные 
  члены делегации, боясь расправы, не вернулись на Кубань. Воспользовавшись недовольством 
  Кубанской Рады, несколько ее членов (почти все черноморцы) по приказу генерала 
  Врангеля были преданы военно-полевому суду. Войсковой атаман и правительство 
  не посмели, а, скорее всего, не захотели, выступить в защиту кубанского парламента, 
  который, уступая силе, вынужден был внести некоторые изменения в кубанскую конституцию. 
  Сущность конституционных изменений заключалась в упразднении законодательной 
  Рады, функции которой передавались Краевой Раде, а также в усилении власти Войскового 
  атамана и правительства, готовых пойти на уступки командованию ВСЮР. Было заявлено 
  и о том, что «Кубанский край мыслит себя неразрывно связанным с Единой, Великой, 
  Свободной Россией».<sup>26</sup><br>

  <br>
  Однако борьба за власть между руководством Кубани и ВСЮР шла с переменным успехом. 
  Не прошло и двух месяцев, с того времени, как Краевая Рада восстановила законодательную 
  Раду и отменила фактически все вырванные у нее уступки. Одним из итогов этой 
  борьбы было оставление фронта кубанскими казаками. Так, если в конце 1918 г. 
  кубанцы составляли 68,75% всех ВСЮР, то к началу 1920 г. их было не более 10%,<sup>27</sup> 
  что, естественно, не могло не отразиться на боеспособности армии Деникина. Таким 
  образом, противостояние казаков (и в первую очередь кубанских самостийников) 
  с добровольцами стало одной из причин неудачи антибольшевицкого движения на 
  Дону и Кубани. <br>
  <br>
  Анализируя процессы, протекавшие в антибольшевицком лагере, можно согласиться 
  с Х.М. Ибрагимбейли, который приводит следующие причины ослабления, а в конечном 
  счете, и поражения ВСЮР: «грабительские методы снабжения армии, насильственные 
  мобилизации населения, коррупция в тыловых органах, грызня между высшим командным 
  составом и кастовое размежевание армии на добровольцев, донцов, кубанцев».<sup>28</sup><br>
  <br>
  Итогом Гражданской войны на Северном Кавказе (Дону и Кубани) явились новороссийская 
  эвакуация и сдача Красной армии казачьих частей на черноморском побережье Кавказа. 
  Оба эти события, с одной стороны, были следствием противоречий между казачьими 
  органами власти и командованием ВСЮР, а с другой — одной из причин углубления 
  этих противоречий в Крыму. <br>
  <br>
  Противоречия между командованием Донской армией и добровольцами привели к тому, 
  что когда встал вопрос об эвакуации, последние, взяв ее под свой контроль, захватили 
  большинство судов и фактически бросили донцов на произвол судьбы. Из Новороссийска 
  были вывезены почти все тыловые учреждения деникинской армии и Добровольческий 
  корпус в полном составе с вооружением и боеприпасами. В то же время большая 
  часть Донской армии, составлявшей подавляющее большинство (после ухода кубанцев) 
  ВСЮР, была оставлена на берегу. Необходимо помнить, что на Кубань вместе со 
  строевыми казаками отступило и гражданское население Дона. Лишь немногим из 
  них удалось выехать в Крым. Страшные по своей дикости, трагичные сцены новороссийской 
  эвакуации запечатлены во многих воспоминаниях ее участников. Всего в районе 
  Новороссийска советские войска захватили в плен около 22 тысяч человек.<sup>29</sup> 
  Из них — не менее 8 тысяч строевых донцов и около 10 тысяч гражданских беженцев 
  с Дона. <br>
  <br>
  IV-й Донской (бывший мамонтовский) корпус (около 18 000 строевых донцов), с 
  присоединившимися к нему двумя бригадами II-го Донского корпуса, был отрезан 
  от новороссийского направления и вынужден двигаться совместно с корпусами Кубанской 
  армии на Туапсе, а затем — к грузинской границе.<sup>30</sup> Положение кубанцев 
  и донцов, отступавших на Туапсе и дальше к грузинской границе, было следующее. 
  Незначительная часть донцов 19 марта эвакуировалась в Крым из Туапсе. 1 мая 
  1920 г. из района Адлера было вывезено на Крымский полуостров около 5 тысяч 
  донцов и 1,5—2 тысячи кубанцев («волки» А.Г. Шкуро).<sup>31</sup> Двумя днями 
  позже в районе Нового города на английских миноносцах были вывезены фактически 
  все казаки IV-го Донского корпуса, за исключением 14-ой и 9-ой донских бригад, 
  которые не смогли пробиться к месту погрузки из-за потока сдававшихся частей 
  Кубанской армии.<sup>32</sup> Таким образом, с полосы от Туапсе до Гагр, в Крым 
  было вывезено не менее 10 000 строевых донцов. <br>
  <br>
  Несмотря на отказ Грузии пропустить на свою территорию белогвардейские части 
  и беженцев, незначительная часть донцов, вместе с кубанцами, перешла грузинскую 
  границу. Учитывая, что некоторое количество донцов распылилось в кубанских горах 
  и станицах, следует признать верными данные кубанского Войскового правительства, 
  отмечавшего сдачу в плен большевикам около 15 000 донцов.<sup>33</sup> По-видимому, 
  большая часть донцов (около 8-9000 человек), попавших в плен, были из обоза 
  донских корпусов или гражданские беженцы. <br>
  <br>
  Из 40-45-тысячной Кубанской армии в плен сдалось не менее 25-30 000 человек. 
  Большинство казаков было разоружено 1-2 мая 1920 г. в районе Сочи. Около полутора 
  тысяч кубанцев перешло в Грузию. Учитывая, что с Черноморского побережья было 
  перевезено в Крым около 2000 кубанских казаков (не считая небольшой части в 
  составе сводного отряда при Добровольческом корпусе), можно сделать вывод, что 
  более 10 000 кубанцев ушло в горы: либо к «зеленым» для дальнейшей борьбы с 
  большевиками, либо в родные станицы. <br>
  <br>
  Таким образом, к лету 1920 г. на Крымском полуострове находилось более 20 000 
  донцов и около 3-4000 кубанцев. Впоследствии большинство кубанцев и донцов, 
  сдавшихся на Черноморском побережье, и, избежавших репрессий со стороны большевиков, 
  для «искупления своей вины кровью» было направлено на польский фронт. Во время 
  войны с Польшей многие из них перешли на сторону поляков и, впоследствии, либо 
  переправлялись в Крым к П.Н. Врангелю, либо были интернированы в Польше и Германии. 
  Часть кубанцев попала к Врангелю в результате разгрома «Армии Возрождения» генерала 
  М.А. Костикова на Кубани. После неудачного десанта генерала С.Г. Улагая с территории 
  Кубани переправилось в Крым 12 000 кубанцев. Всего к ноябрю 1920 г. на Крымском 
  полуострове находилось около 30 тысяч донцов и не менее 20 тысяч кубанцев. <br>
  <br>
  Что касается взаимоотношений между казаками и белым командованием, то в Крыму 
  они приобрели совсем другой оттенок, став более жесткими и бескомпромиссными 
  (не считая ряда тактических уступок) со стороны генерала Врангеля. В первую 
  очередь, это было связано с тем, что Крымский полуостров не являлся казачьей 
  территорией, а, следовательно, исчезал один из факторов, усиливавших влияние 
  казачества. <br>
  <br>
  В то же время нельзя согласиться с А.А. Гордеевым, утверждавшим, что «командование 
  Добровольческой армии было избавлено от необходимости считаться с правительствами 
  Дона, Кубани и Терека».<sup>34</sup> Оказавшись вне пределов казачьей территории, 
  руководство ВСЮР, конечно, чувствовало себя более свободно. Это выразилось, 
  в частности, в реорганизации Военного Совета. Если раньше в нем преобладали 
  донцы, то теперь из 20 человек Военного Совета только 3 были донскими генералами. 
  Далее «Врангель продолжает, начатое Алексеевым, Корниловым и Деникиным дело 
  — подчинение себе всех контрреволюционных течений на юге России, но не посредством 
  подчинения себе политических верхушек этих течений, а, наоборот, путем устранения 
  этих верхушек».<sup>35</sup> В частности был уволен со службы и выслан за границу 
  командующий Донской армией генерал В.И. Сидорин. Интересен тот факт, что он 
  был уволен «по соглашению с Донским атаманом».<sup>36</sup><br>
  <br>
  В то же время совсем не считаться с казачеством Врангель ни мог. Во-первых, 
  казаки все еще составляли не менее половины Русской армии, а, во-вторых, именно 
  казачество могло дать новые силы Белому движению. <br>
  <br>
  В свою очередь казаки окончательно разуверились в возможности освобождения России 
  от власти большевиков, а лидеры казачества не могли не чувствовать шаткость 
  своего положения в отрыве от казачьих областей. Все это привело, в конечном 
  счете, к обращению атаманов Дона и Кубани к правительствам Англии и Франции 
  с просьбой о посредничестве в деле разрешения военного конфликта между Советской 
  Россией и белыми правительствами казачьих территорий и о признании независимости 
  последних. Правда, как уверял атаман Всевеликого Войска Донского А.П. Богаевский, 
  у него не было и в мыслях сепаратного (со стороны Дона) мира с большевиками. 
  Однако Врангель, встревоженный возможностью остаться один на один с Красной 
  армией, принудил казачью верхушку подписать 15 апреля 1920 г. соглашение, а 
  22 июля того же года — заключить договор о казачьем союзе под своим руководством. 
  В нем признавалось полное военное руководство Врангеля, в том числе и казачьими 
  войсками. Кроме того, казачьи атаманы обязались вести все внешние сношения только 
  с разрешения и при посредстве Верховного главнокомандующего Русской армией. 
  В обмен на подчинение себе казачьей верхушки, Врангель обещал атаманам полную 
  автономию и независимость в отношении внутреннего гражданского устройства. <br>
  <br>
  Договор Врангеля с казачьими атаманами был использован оппозиционными силами, 
  которые подвергли его резкой критике. Параллельно президиум кубанской Краевой 
  Рады объявил договор недействительным. При этом члены президиума ссылались на 
  то, что договор не имел подписи председателя краевой Рады. Однако этот Демарш 
  со стороны кубанцев не помешал Врангелю 4 августа 1920 г. заявить о союзе, заключенном 
  им с казачеством «для общей борьбы против большевиков».<sup>37</sup><br>
  <br>
  В целом, обещание независимости казачьих земель помогло Врангелю, с одной стороны, 
  добиться подчинения со стороны казачьей верхушки, а с другой — удержать ею доверия 
  казаков. О подчинении казачества свидетельствует и тот факт, что в Крыму уже 
  не существовало ни Донской, ни Кубанской армии. Первая была переформирована 
  в донской казачий корпус, командиром которого был назначен сторонник Врангеля 
  генерал Ф.Ф. Абрамов. Из кубанцев была сформирована Кубанская бригада. Все эти 
  части входили в одну армию, которая подчеркнуто именовалась «Русской». <br>
  <br>
  К ноябрю 1920 г. казаки, находившиеся на Крымском полуострове, вместе со всей 
  Русской армией оказались перед выбором: сдаться на милость большевиков или покинуть 
  неприветливый остров и уехать на чужбину с надеждой на скорое возвращение в 
  родные края. <br>
  <br>
  Надо отдать должное Врангелю: вывод 150-тысячной массы беженцев из Крыма под 
  напором частей Красной армии прошел более организованно, чем новороссийская 
  эвакуация. Этому способствовали и объективные причины. Во-первых, в Крыму было 
  больше портов, чем на черноморском побережье, что позволило уменьшить толчею, 
  а, следовательно, снять напряженность и панику. Во-вторых, заранее были подготовлены 
  корабли с запасом топлива и продовольствия. В-третьих, задолго до крымской катастрофы 
  уже был составлен план эвакуации, что дало возможность без лишней суеты организовать 
  погрузку. В-четвертых, слияние частей Русской армии в единый боевой организм, 
  снятие напряженности в отношениях между лидерами казачества и руководством ВСЮР, 
  которое было характерно для деникинского периода борьбы, дали возможность избежать 
  того ажиотажа и вражды, которые сопутствовали новороссийской эвакуации. Равномерное 
  распределение портов погрузки между донцами, кубанцами и другими частями армии 
  также способствовало относительно спокойной обстановке при эвакуации. <br>
  <br>
  Однако не следует идеализировать эвакуацию, как это делали в своих воспоминаниях 
  некоторые сторонники Врангеля. В Крыму было все: и грабежи, и пожары, и суета, 
  и панические настроения. Но все это проявлялось в гораздо меньшей степени, чем 
  в Новороссийске и не помешало погрузиться всем, кто желал уехать. Большинство 
  донцов и кубанцев вынуждены были нести тяжелое бремя эмигранта. Не менее тяжелым 
  было положение и значительной части казаков, оставшихся в своих родных краях. 
  Что же касается дальнейших политических исканий донского и кубанского казачества, 
  то они продолжились в эмиграции (также как и противостояние с Врангелем). Однако 
  это является темой отдельного исследования. <br>
