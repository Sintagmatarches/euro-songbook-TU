<div class='refren'>своему киргизину: </div>
"Ты поезжай, поезжай во дикую степь, <br>
<div class='refren'>во дикую степь,  </div>
Ты найди, найди нянюшку, <br>
<div class='refren'>найди нянюшку".  </div>
Как побежал же злой киргизушка, <br>
<div class='refren'>злой киргизушка,  </div>
И нашел же он русску бабушку, <br>
<div class='refren'>русску бабушку. </div>
