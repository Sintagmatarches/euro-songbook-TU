<div class='refren'>златом-серебром. </div>
Отвез же злой киргизушка ее на свою сторонушку, <br>
<div class='refren'>на свою сторонушку. </div>
Пустил же он ее на свои резвы ноженьки, <br>
<div class='refren'>на свои резвы ноженьки. </div>
Как пришла родна маменька на свою родну сторонушку,<br>
<div class='refren'>на свою родну сторонушку, </div>
Как и стала же она объяснять о своей родной доченьке,<br>
<div class='refren'>о своей родной доченьке, </div>
О своей родной доченьке родному батюшке, <br>
<div class='refren'>родному батюшке,</div>
Как живет она во орде киргизской, <br>
<div class='refren'>во орде киргизской, </div>
Нажила она родных детушек, <br>
<div class='refren'>родных детушек,</div>
Как по матушке они - крови русской, <br>
<div class='refren'>крови русской, </div>
А по батюшке - крови киргизской, <br>
<div class='refren'>крови киргизской.</div>
