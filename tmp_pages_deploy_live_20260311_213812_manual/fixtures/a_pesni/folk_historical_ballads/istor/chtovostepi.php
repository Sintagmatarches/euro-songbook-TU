<div class='refren'>своего киргизушку </div>
Отвезти ее родну маменьку на свою сторонушку, <br>
<div class='refren'>на свою сторонушку. </div>
"Ты прости, прощай, моя маменька, <br>
<div class='refren'>моя маменька,</div>
Ты дай мне свое благословеньице навеки нерушимое, <br>
<div class='refren'>навеки нерушимое". </div>
