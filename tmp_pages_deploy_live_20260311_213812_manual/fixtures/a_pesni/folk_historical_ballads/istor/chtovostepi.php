<div class='refren'>приговаривала:  </div>
"Ты, качу, баю, мое дитятко, <br>
<div class='refren'>мое дитятко; </div>
Ты по матушке - крови русской, <br>
<div class='refren'>крови русской, </div>
А по батюшке - крови басурманской, <br>
<div class='refren'>крови басурманской". </div>
Как за дверью дочь подслушала, <br>
<div class='refren'>дочь подслушала. </div>
Растворяла же она двери киргизские, <br>
<div class='refren'>двери киргизские; </div>
Выходила она к родной матери, <br>
<div class='refren'>к родной матери, </div>
И кидалась она на шею матери, <br>
<div class='refren'>на шею матери: </div>
"Ты родимая, родна маменька, <br>
<div class='refren'>родна маменька,</div>
Ты не знала, где твоя доченька, <br>
<div class='refren'>твоя доченька". </div>
Как сказала же княгиня самому хану, <br>
<div class='refren'>самому хану: </div>
<br>
"Отвезите родну мою маменьку на свою сторонушку, <br>
<div class='refren'>на свою сторонушку". </div>
