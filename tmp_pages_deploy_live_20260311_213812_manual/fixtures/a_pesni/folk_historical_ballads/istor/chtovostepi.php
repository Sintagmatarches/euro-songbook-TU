<div class='refren'>а я слезу к вам".  </div>
Как слезала же красна девица, <br>
<div class='refren'>красна девица,  </div>
С того ли стога несв<strong>е</strong>ршенного,<br>
<div class='refren'>несв<strong>е</strong>ршенного.  </div>
