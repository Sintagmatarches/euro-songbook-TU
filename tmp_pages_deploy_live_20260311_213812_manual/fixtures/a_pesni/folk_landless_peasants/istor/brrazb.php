 <h1>БРАТЬЯ-РАЗБОЙНИКИ 
И СЕСТРА
<br>
 ПО КРАЙ МОРЯ, МОРЯ СИНЕГО...</h1>
<br>
По край моря, моря синего, 
  <br>
  По край моря ай Веряжского, <br>
  По край синего моря Веряжского, <br>
  Там стояла да хоромина некрытая, <br>
  А некрытая хоромина, немшоная. <br>
  Там жила-была вдова благочестивая, <br>
  Был<strong>о</strong> у вдовушки девять сынов, <br>
  Во десятыих была одинакая дочь. <br>
  И эти все сынов<strong>а</strong> в разбой пошли, <br>
  Оставалася дочка единёшенька. <br>
  Как из-за синего моря Веряжского <br>
  Наехали на дочку к нею св<strong>а</strong>това, <br>
  Она выдала дочку за синё море, <br>
  За этого за гостя за торгового. <br>
  Она там год жила — не стоснулася, <br>
  Другой жила — в уме не было, <br>
  На третий на годочек стосковалася, <br>
  Она у мужа у света подавалася, <br>
  У всёй семье сдоложилася. <br>
  Состроил ей муж червончатой кораб, <br>
  Он нос делал по-змеиному, <br>
  Корму делал по-звериному, <br>
  Нос грузил чистым с<strong>е</strong>ребром, <br>
  Середочку грузил красным золотом, <br>
  Корму грузил скатным жемчугом. <br>
  Они сели с мужем в лодочку и поехали. <br>
  Тут не темная ноченька сустигла их, <br>
  Не частые дождики обсыпали — <br>
  Наехали воры-разбойники, <br>
  Они гостя торгового зарезали, <br>
  Зарезали и в воду бросили, <br>
  Они жёночку-рязаночку в полон брали, <br>
  В полон брали, обесчестили. <br>
  Все эти разбойнички спать легли, <br>
  Меньший разбойничек не спит, не лежит,<br>
  Не спит, не лежит, думу думает,<br>
  У жёночки-рязаночки выспрашивает: <br>
  «Скажи, жёночка-рязаночка, с коёй орды, <br>
  С коей орды, с коей страны?» — <br>
  «Уж мы жили-жили по край моря, <br>
  По край синего моря Веряжского, <br>
  Там стояла хоромина некрытая, <br>
  Некрытая хоромина, немшоная. <br>
  Во той было хоромине некрытоей <br>
  Жила-была вдова благочестивая, <br>
  Было у вдовушки дев<strong>я</strong>ть сынов, <br>
  Во десятыих была одинакая дочь. <br>
  У ней все сынов<strong>а</strong> во разбой пошли, <br>
  Оставалася дочка одинёшенька. <br>
  Из-за синего моря Веряжского <br>
  Наехали на дочку к ней сватова, <br>
  Она выдала дочку за синё море, <br>
  За этого за гостя за торгового». <br>
  Тут разбойничек расплакался. <br>
  «Вы ставайте-ко, братцы родимые! <br>
  Мы не гостя торгового зарезали, <br>
  Зарезали и в воду бросили, <br>
  Мы зарезали зятя любимого. <br>
  Не жёночку-рязаночку в пол<strong>о</strong>н брали, <br>
  В полон брали и обесчестили, <br>
  Обесчестили сестрицу родимую». <br>
