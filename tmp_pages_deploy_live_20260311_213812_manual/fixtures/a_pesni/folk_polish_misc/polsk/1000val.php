  <strong>Pułk czwarty<br>
  (Четвертый полк) </strong><br>
  <br>
  Słowa: Julius Mosen<br>
  Przekład: Jan Nepomucen Kamiński <br>
  <br>
  Walecznych tysiąc opuszcza Warszawę, <br>
  Przysięga klęcząc: naszym świadkiem Bóg! <br>
  Z bagnetem w ręku pójdziem w świętą sprawę, <br>
  Śmierć naszym hasłem, niechaj zadrży wróg. <br>
  Już dobosz zabrzmiał, już sojusz zawarty, <br>
  Z panewką próżną idzie w bój pułk czwarty. <br>
  <br>
  Wiadoma światu ta sławna Olszyna, <br>
  Gdzie nieprzyjaciel twardym murem stał! <br>
  Paszcz tysiąc sieje, rzeź się krwawa wszczyna, <br>
  Już mur zwalony, nie раdł ani strzał;<br>
  Okropny postrach poniósł tłum rozżarty, <br>
  Spokojnie wrócił do Pragi pułk czwarty. <br>
  <br>
  Pod Ostrołęką wróg się dumno zrywa, <br>
  Otacza wolnych dzikiej hordy wał, <br>
  Śmierć albo życie, tu wyboru niema, <br>
  Z bagnetem w ręku, nie раdł ani strzał!<br>
  I już dla naszych odwrót jest otwarty, <br>
  I któż to zdziałał? a był to pułk czwarty! <br>
  <br>
  On to po ciężkiej i krwawej rozprawie, <br>
  Jak ostry piorun, jako bitwy pan, <br>
  Ponuro wracał ku tęsknej Warszawie, <br>
  Krew obmył w Wiśle już z przyschniętych ran. <br>
  Czerwono płynie w morzu prąd niestarty, <br>
  Krew to walecznych, przelał ją pułk czwarty. <br>
  <br>
  Daremne męstwo! Ojczyzna zgubiona; <br>
  Ach! nie pytajcie, kto spełnił ten czyn; <br>
  Zabójczy potwór wyszedł z matki łona, <br>
  Ojczyny zgubą jest wyrodny syn! <br>
  W kawałki znowu kraj polski rozdarty,<br>
  Krwawymi łzami zapłakał pułk czwarty. <br>
  <br>
  Żegnajcie bracia, których nam przy boku <br>
  Za świętą sprawę wzięła śmierci dłoń! <br>
  Wam lepszy udział dostał się w wyroku, <br>
  Nam chytra zdrada wzięła z ręki broń. <br>
  Jak biedny tułacz na kiju oparty,<br>
  W kraj obcy idzie nasz dzielny pułk czwarty. <br>
  <br>
  Dziesięciu mężnych obłąkanym wzrokiem <br>
  Przechodzi chwiejno pod granicy słup, <br>
  Ciekawem zewsząd patrzą na nich okiem, <br>
  Z nich każdy idzie, jak bez życia trup;<br>
  Kto idzie! stójcie! krzykną pruskie warty, <br>
  Му to dziesięciu — cały nasz pułk czwarty. <br>
