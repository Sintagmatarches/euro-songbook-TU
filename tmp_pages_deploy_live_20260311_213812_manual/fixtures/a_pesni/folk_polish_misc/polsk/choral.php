	<span class="header">  <strong>Chorał <br>
  (Хорал)</strong><br></span>
  <br>
  Muzyka Józefa Nikorowicza<br>
  S&#322;owa Kornela Ujejskiego<br>
  <br>
  Z dymem pożarów, z kurzem krwi bratnej,<br>
  Do Ciebie, Panie, bije ten głos,<br>
  Skarga to straszna, jęk to ostatni,<br>
  Od takich modłów bieleje włos.<br>
  My już bez skargi nie znamy śpiewu,<br>
  Wieniec cierniowy wrósł w naszą skroń,<br>
  Wiecznie, jak pomnik Twojego gniewu, <br>
  Sterczy ku Tobie błagalna dłoń. <br>
  <br>
  Ileż to razy Tyś nas nie smagał! <br>
  А my nie zmyci ze świeżych ran, <br>
  Znowu wołamy: "On się przebłagał, <br>
  Во On nasz Ojciec, bo On nasz Pan!&quot; <br>
  I znów powstajem w ufności szczersi, <br>
  А za Twą wolą zgniata nas wróg, <br>
  I śmiech nam rzuca jak głaz na piersi: <br>
  "А gdzież ten Ojciec, а gdzież ten Bóg?&quot; <br>
  <br>
  I patrzym w niebo, czy z jego szczytu <br>
  Sto słońc nie spadnie wrogom na znak... <br>
  Cicho i cicho - pośród błękitu <br>
  Jak dawniej buja swobodny ptak. <br>
  Owóż w zwątpienia strasznej rozterce, <br>
  Nim naszą wiarą ocucim znów, <br>
  Bluźnią Ci usta, choć płacze serce: <br>
  Sądź nas ро sercu, nie według słów! <br>
  <br>
  О Panie, Panie! ze zgrozą świata <br>
  Okropne dzieje przyniósł nam czas: <br>
  Syn zabił ojca, brat zabił brata, <br>
  Mnóstwo Kainów jest pośród nas. <br>
  Ależ, о Panie! oni nie winni, <br>
  Choć naszą przyszłość cofnęli wstecz, <br>
  Inni szatani byli tam czynni, <br>
  О, rękę karaj, nie ślepy miecz! <br>
  <br>
  Patrz! Му w nieszczęściu zawsze jednacy, <br>
  Na Twoje łono, do Twoich gwiazd. <br>
  Modlitwą płyniem jak senni ptacy, <br>
  Со lесą spocząć wśród własnych gniazd. <br>
  Osłoń nas, osłoń ojcowską dłonią, <br>
  Daj nam widzenie przyszłych Twych łask, <br>
  Niech kwiat męczeński uśpi nas wonią, <br>
  Niech nas niebiański otoczy blask. <br>
  <br>
  I z archaniołem Twoim na czele <br>
  Pójdziemy wszyscy na straszny bój, <br>
  I na drgającym szatana ciele <br>
  Zatkniemy sztandar zwycięski Twój! <br>
  Zbłąkanym braciom otworzym serca, <br>
  Winę ich zmyje wolności chrzest, <br>
  Wtenczas usłyszy podły bluźnierca <br>
  Odpowiedź naszą: "Bóg był i jest&quot;. <br>
  <br>
  <br>
  <img src="chor.gif" width="756" height="883"><br>
  <br>
  <br>
  Kiedy w marcu 1846 roku upadło powstanie krakowskie zakończone nesławną "rabacją 
  galicyjską&quot; - krwawą rzezią polskiej szlachty i spaleniem 500 dworów - 
  mieszkający w Zboiskach pod Lwowem młody kompozytor Józef Nikorowicz napisał 
  melodię-modlitwę, którą nazwał <em>Chorałem</em>. W tym samym miesiącu odwiedził 
  Nikorowicza lwowski poeta Kornel Ujejski. Ро wysłuchaniu melodii <em>Chorału</em>, 
  jeszcze tego samego dnia, napisał do niej słowa. <br>
  <br>
  Początek wiersza: &quot;Z dymem pożarów...&quot; uznano potem za tytuł tej pieśni. 
  Towarzyszyła ona Polakom także podczas I wojny światowej. W czerwcu 1919 roku 
  17-letni wówczas Jan Kiepura, będąc żolnierzem Pułku Strzelców Bytomskich, tuż 
  przed wybuchem I powstania śląskiego pisał w liście do rodziców: "Czasem pod 
  wieczór, gdy wszystko jest cicho, jak zaśpiewamy <em>Z dymem pożarów</em>, to 
  duszę rozpiera potęga tego uczucia, które powstaje pod wpływem melodii tej pieśni...&quot; 
  <br>
  <br>
  ***<br>
  <br>
  <strong>Józef NIKOROWICZ (1827-1890)</strong>, kompozytor, twórca utworów fortepianowych i pieśni, 
  związany ze Lwowem, jeden z czołowych przedstawicieli Ormian polskich w naszej 
  kulturze. Skomponował chorał <em>Z dymem pożarów</em> (sł. К. Ujejski). <br>
  <br>
  <strong>Kornel UJEJSKI (1823-1897)</strong>, poeta i publicysta. Sławę przyniósł mu poemat Maraton 
  (1845) oraz cykl wierszy <em>Skargi Jeremiego</em> (1847). W ramach tego cyklu 
  znalazł się wiersz<em> Chorał </em>(<em>Z dymem pożarów</em>) napisany do muzyki 
  J. Nikorowicza. Вуł twórcą i orędownikiem poezji patriotycznej, głosząc - jako 
  publicysta - tezę о obowiązku pisarza wobec narodu. Вуł też autorem interesujących 
  prób przełożenia wrażeń muzycznych na język poezji.<br>
  <br>
  <em>Wac&#322;aw Panek. Hymny polskie. Warszawa, Wydawnictwa Szkolne 
  i Pedagogiczne, 1997.</em><br>
 <br>
  <br>
  Написано в марте 1846 г. после поражения Краковского восстания. Живущий в Збоисках 
  под Львовом молодой композитор Юзеф Никорович написал мелодию-молитву. Тогда 
  же, в марте, его навестил львовский поэт Корнелий Уйейский, который, услышав 
  мелодию, в тот же день написал слова. Была популярна в том числе и во время 
  во время Первой мировой войны. В России ее мелодия была положена Модестом Мусоргским 
  в основу романса 1877 г. <a href="../army/torzsmerti.php">&quot;Торжество смерти&quot;</a> 
  на слова Арсения Голенищева-Кутузова.<br>
  <br>
  На эту мелодию сложена также освободительная песня поляков Силезии <a href="walczslask.htm">&quot;Walcz, 
  Śląski ludu!&quot;</a> (sł: L. Szamot), котора содержит не только обращение 
  к богу, как в исходной песне, но и его ответ с небес: <br>
  <br>
  <em>Walcz, Śląski ludu, za wiarę Twoją! <br>
  Walczących tylko wspiera ma dłoń!..<br>
  <br>
  (Сражайся, народ Силезии, за свою веру,<br>
  Только сражающихся поддерживает моя длань!..)</em><br>
  <br>
  <br>
  <strong>Краковское восстание (Powstanie Krakowskie) 1846 г.</strong><br>
  <br>
  В ночь с 20 на 21 февраля 1846 г. в Кракове польские повстанцы атаковали части австрийского генерала 
  Цоллина (Collin). 22 февраля австрийцы в городе были разбиты. Власть перешла к Национальному правительству 
  Республики, которое издало манифест об освобождении крестьян с землей. Повстанцам удалось собрать 
  6 тысяч бойцов. 24 февраля провозглашена диктатура генерала Тыссовского, а 4 марта 
  Краков был занят стянутыми русскими и австрийскими войсками. Повстанцы отступили 
  в Галицию, Краковская республика пала. В Галиции австрийцы распустили 
  слух, что краковские повстанцы идут, чтобы отобрать последние 
  права местных крестьян. Идущих с 
  хоругвями повстанцев встретили толпы вооруженных местных жителей, началась резня 
  повстанцев и местной польской шляхты (&quot;галицийский бунт&quot;), сожжено 500 дворов поляков. Тыссовский 
  эмигрировал в Пруссию.<br>
