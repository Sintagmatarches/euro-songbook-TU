	<span class="header"><strong>Pod samowarem <br>
  (У самовара)</strong><br></span> 
  <br>
  Muzyka: Andrzej Włast<br>
  Słowa: Fanny Gordon<br>
  <br>
  Znów jest maj, ten sam, <br>
  Kasztany kwitną znów ogromnie. <br>
  Naplewat' mnie tam! <br>
  Ja jedno wiem i jedno pomnę: <br>
  <br>
  Pod samowarem siedzi moja Masza, <br>
  Ja mówię «tak», a ona mówi «nie». <br>
  Jak w samowarze kipi milość nasza, <br>
  Ja gryzę pestki, ona na mnie klnie. <br>
  <br>
  Potem nagle po całusku <br>
  Wydziela do herbaty na prikusku. <br>
  Pod samowarem siedzi moja Masza <br>
  I jak herbata tak naciąga mnie! <br>
  <br>
  Przyszli, wzięli nam <br>
  Firanki, szubę, stół i łóżko. <br>
  Naplewat' mnie tam, <br>
  Bo ja o jednym myślę duszko. <br>
  <br>
  Pod samowarem siedzi moja Masza… <br>
  <br>
  1929<br>
  <br>
  <br>
        Фокстрот написан в 1929 году Фанни Гордон (польск. Fanny Gordon), в будущем 
        - Фаиной Квятковской (польск. Faina Kwiatkowska), проживавшей тогда в 
        Польше уроженкой Ялты, для варшавского кабаре &quot;Морское око&quot; 
        (&quot;Morskie oko&quot;) на слова владельца кабаре Анджея Власта (Andrzej 
        Włast). По предложению фирмы &quot;Polydor Records&quot; Фанни Гордон 
        написала также русский текст из двух строф для записи на пластинку, которая 
        в 1933 году поступила в продажу в Риге (см. <a href="../../drugije/usamovara.php">&quot;У 
        самовара&quot;</a>, там же см. ноты).<br>
  <br>
        В том же 1933 году русская версия заимствована Утесовым и в феврале 1934 
        года записана им в Москве без указания автора. <br>
  <br>
  Другой широко известный исполнитель русской версии - Петр Лещенко, записавший 
  ее на пластинку венского филиала фирмы &quot;Columbia&quot; в 1933 году (по 
  другим данным - в 1936-37 гг.), добавив к двум строфам Фанни Гордон еще две.<br>
  <br>
  У фокстрота есть и литовский вариант текста (лит. «Palangos jūroj»), который 
  не позднее 1931 года исполнял в Каунасе и записал на пластинку один из родоначальников 
  литовской эстрады Даниелюс Дольскис (Даниил Дольский, лит. Danielius Dolskis). 
  С текстами Власта и Гордон он не связан:<br>
  <br>
  <em>Palangos jūroj nuskendo mano meilė<br>
  Ir šaltos bangos jos neatiduos... <br>
  <br>
  В Паланге в море утонула любовь моя<br>
  И холодные волны не вернут ее...</em><br>
  <br>
  Подробнее см. Георгий Сухно. <a href="../../emigr/a-pollesch.php">Польские песни 
  в репертуаре Петра Лещенко</a> (Польша, 2009).<br>
