Казачья песня. Существует также на русском языке - см. <a href="../kazaki/priluzku.htm">&quot;Ой, 
при лужку, при лужку&quot; (русская версия</a>; там же ноты, причем мелодия другая). 
Восходит к русскому стихотворению Александра Дуропа <a href="../popular20/kaznarodine.htm">&quot;Казак 
на родине&quot;, </a>опубликованному в 1818 году. В 1830-е годы оно появилось в песенниках, а затем в фольклорных записях как русская казачья песня &quot;Ой, при лужку, при 
лужку&quot;. От первоначального авторского текста в фольклоре осталось очень мало, 
и финал изменился: у поэта девушка не дождалась парня и отдала свое сердце другому. 
Когда возник украинский вариант (он идентичен русскому) - одновременно, после 
(или раньше?) русского - непонятно, да и не так принципиально. Песня сейчас общая.<br>
