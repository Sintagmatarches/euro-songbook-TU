	<div align="justify"> <span class="text"> <span class="header"><strong>Juillet 
        1936<br>
        (Июль 1936)</strong></span> <br>
        <br>
        Serge Utge Royo<br>
        <br>
        Juillet 1936 dans les casernes catalanes <br>
        La mort bute sur les milices et le peuple compte ses armes <br>
        Dans les villages et les hameaux les paysans groupent les terres <br>
        En un seul et riche morceau et passe le vent libertaire <br>
        <br>
        Je pense à vous vieux compagnons dont la jeunesse est à la douane <br>
        et pardonner si ma chanson vous refait mal à votre Espagne <br>
        Mais j'ai besoin de vous apprendre j'ai envie de vous ressembler <br>
        Je gueulerai pour qu'on entende ce que vous m'avez enseigné <br>
        <br>
        Donne-moi ta main camarade <br>
        Prête-moi ton coeur compagnons <br>
        Nous referons les barricades <br>
        Comme hier la confédération <br>
        <br>
        A quelques heures de Barcelone se sont groupés des menuisiers <br>
        Et sans patron tout refonctionne on sourit dans les ateliers <br>
        Sur la place de la mairie qu'on a changé en maternelle <br>
        Des femmes ont pris la blanchisserie et sortent le linge au soleil <br>
        <br>
        Donne-moi ta main camarade <br>
        Prête-moi ton coeur compagnons <br>
        Nous referons les barricades <br>
        Et la vie, nous la gagnerons <br>
        <br>
        Tandis que quelques militaires font leur métier de matadors <br>
        Des ouvriers, des ouvrières détruisent une prison d'abord <br>
        Là-bas, c'est la mort qui s'avance tandis qu'ici: Ah madame c'est l'Anarchie 
        <br>
        La liberté dans l'espérance ils ont osé la vivre aussi <br>
        <br>
        Dame tu mano companero <br>
        I presta me tu corazon <br>
        Barricadas leventaremos <br>
        Como ahier la confederacion <br>
        <br>
        <br>
        <strong>Перевод:</strong><br>
        <br>
        Июль 1936: в каталонских казармах<br>
        Смерть спотыкается о милицию, и народ подсчитывает оружие.<br>
        В деревнях и поселках крестьяне объединяют земельные участки<br>
        В один изобильный кусок земли, и дуют либертарные ветры.<br>
        <br>
        Я думаю о вас, старые товарищи, чья юность миновала незаметно, <br>
        Простите меня, если моя песня разбередила ваши испанские раны.<br>
        Но мне очень нужно сообщить вам, что я хочу собрать вас.<br>
        Мне хочется кричать на весь мир, чтобы все услышали, чему вы меня научили.<br>
        <br>
        Протяни мне руку, товарищ,<br>
        Открой мне свое сердце, <br>
        И мы снова воздвигнем баррикады,<br>
        Как вчера – Конфедерация.<br>
        <br>
        За несколько часов в Барселоне плотники объединяются<br>
        И снова запускают все без хозяев; в мастерских сверкают улыбки.<br>
        На площади перед мэрией устроили детский сад,<br>
        Женщины занялись стиркой и развесили белье на солнце. <br>
        <br>
        Протяни мне руку, товарищ,<br>
        Открой мне свое сердце, <br>
        Мы снова построим баррикады<br>
        И отвоюем нашу жизнь.<br>
        <br>
        Пока несколько военных выполняют свою профессию убийц, <br>
        Рабочие и работницы прежде всего разрушают тюрьму.<br>
        Там, внизу, наступает смерть, а здесь… «Ах, мадам, это Анархия!»<br>
        Свобода в надежде – они осмелились так жить.<br>
        <br>
        Протяни мне руку, товарищ,<br>
        И открой мне свое сердце.<br>
        Мы воздвигнем баррикады,<br>
        Как вчера – Конфедерация.<br>
