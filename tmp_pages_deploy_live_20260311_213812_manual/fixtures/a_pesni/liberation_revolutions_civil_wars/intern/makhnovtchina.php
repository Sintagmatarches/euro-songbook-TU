  Французская песня о партизанах-махновцах, приписываемая Этьену Рода-Жилю (есть 
  и другие версии авторства), на мелодию русской партизанской песни Петра Парфенова 
  <a href="../grvojna/kr/podolinam.php">«По долинам и по взгорьям»</a> (1920, 
  там же см. ноты). Предположительно, написана в 1960-е годы. <br>
  <br>
  <strong>Этьен Рода-Жиль</strong> – композитор и писатель, родился в 1941 году 
  во Франции в семье испанских республиканцев; кроме прочего, создавал музыку 
  для песен Ванессы Паради («Joe le taxi» и др.) и Жюльет Греко. Один из авторов 
  мюзикла о Великой французской революции «&Ccedil;a ira!».<br>
  <br>
  <br>
  Есть версии этой песни на польском (<a href="machno-pl.php">&quot;Machnowszczina&quot;</a>), 
