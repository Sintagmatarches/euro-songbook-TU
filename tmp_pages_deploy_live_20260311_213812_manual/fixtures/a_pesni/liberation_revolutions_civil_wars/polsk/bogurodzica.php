  <br>
  <br>
  <em>Bogurodzica</em> jest niew&#261;tpliwie najwi&#281;ksz&#261; chlub&#261; 
  i najcenniejszym utworem polskiego &#347;piewnika narodowego. Znamy j&#261; 
  dzi&#347; jako pierwsz&#261; pie&#347;&#324; hymniczn&#261; w j&#281;zyku polskim. 
  Jest najstarsz&#261; - w&#347;r&oacute;d odkrytych - pie&#347;ni&#261; bojow&#261; 
  rycerstwa polskiego а jednocze&#347;nie pie&#347;ni&#261; religijn&#261;. Od 
  jej pojawienia si&#281; w historii rozpoczyna si&#281; - charakterystyczne dla 
  naszych dziej&oacute;w - wsp&oacute;&#322;istnienie pie&#347;ni religijnych 
  i wojskowych (rycerskich, &#380;o&#322;nierskich, ch&#322;opskich), wyst&#281;puj&#261;cych 
  we wsp&oacute;lnym nurcie pie&#347;ni patriotycznej. (Wiele znacz&#261;cych 
  w historii polskiego &#347;piewu narodowego pie&#347;ni wojskowych zwraca&#322;o 
  si&#281; do Boga w swym tek&#347;cie i motywach melodycznych. Jak r&oacute;wnie&#380; 
  wiele pie&#347;ni, pocz&#261;tkowo &#347;piewanych tylko przy uroczysto&#347;ciach 
  religijnych, zacz&#281;&#322;o pelni&#263; rol&#281; pie&#347;ni &#380;o&#322;nierskich, 
  bitewnych.) Dlatego <em>Bogurodzica</em> sta&#322;a si&#281; symbolem &#322;&#261;czno&#347;ci 
  religijno-patriotycznej w pie&#347;niarstwie polskim. <br>
  <br>
  Pierwszy znany nam r&#281;kopis jej tekstu pochodzi z roku 1407 (tzw. &quot;r&#281;kopis 
  krakowski I&quot; lub &quot;przekaz Аа&quot;). Odnaleziono go przypadkiem, bowiem 
  tekst ten by&#322; zapisany na karcie przyklejonej do wewn&#281;trznej strony 
  ok&#322;adki ksi&#281;gi z &#322;aci&#324;skimi kazaniami pisanymi przez Macieja 
  z Grochowa, wikariusza w Kcyni w 1407 roku. (Znajduje si&#281; w Bibliotece 
  Jagiello&#324;skiej, opatrzony sygnatur&#261; 1619.) Wed&#322;ug tego&#380; 
  najstarszego przekazu podano wy&#380;ej tekst pie&#347;ni. <br>
  <br>
  Pierwszy druk <em>Bogurodzicy</em> z 1506 roku (zawarty w wydanym w Krakowie 
  <em>Statucie &#321;askiego</em>) jest te&#380; najstarszym drukowanym polskim 
  tekstem poetyckim. Kolejne chronologicznie zapisy tekstu przynosz&#261; coraz 
  wi&#281;cej, dopisywanych tej pie&#347;ni, zwrotek. Pod koniec XV stulecia, 
  kiedy <em>Bogurodzica</em> sta&#322;a u szczytu swojej hymnicznej popularno&#347;ci, 
  zanotowano a&#380; 27 strof tej pie&#347;ni. Spo&#347;r&oacute;d znanych nam 
  oko&#322;o 50 przekaz&oacute;w z XV wieku - kilka zawiera te&#380; strofy modlitwy 
  za kr&oacute;la. Na przyk&#322;ad za czas&oacute;w Kazimierza Jagiello&#324;czyka 
  w II po&#322;owie XV wieku &#347;piewano w <em>Bogurodzicy</em> strof&#281;: 
  &quot;Popro&#347;my&#380; ju&#380; Boga za kr&oacute;la polskiego i za dziatki 
  jego. Aby je B&oacute;g uchowa&#322; ode wszego z&#322;ego&quot;. Ву&#322; to 
  pocz&#261;tek rozwijaj&#261;cego si&#281;, zw&#322;aszcza w nast&#281;pnym stuleciu, 
  nurtu &quot;&#347;piew&oacute;w za kr&oacute;la&quot;, о kt&oacute;rych Wojciech 
  J. Podg&oacute;rski pisa&#322;: &quot;Podobnie jak s&#322;owa pacierza jednoczy&#322;y 
  wsp&oacute;&#322;wyznawc&oacute;w, apostrofa do kr&oacute;la utrwala&#322;a 
  poczucie wi&#281;zi narodowej.&quot; <br>
  <br>
  Nie jest nam dzi&#347; znany imiennie ani tw&oacute;rca tekstu, ani kompozytor 
  <em>Bogurodzicy</em>. О melodii tej pie&#347;ni pisa&#322; najwybitniejszy badacz 
  muzyki polskiego &#347;redniowiecza, ks. prof. Hieronim Feicht: &quot;Powa&#380;na, 
  majestatyczna, rycerska, si&#281;gaj&#261;ca szczyt&oacute;w artyzmu profesjonalnego, 
  nie mie&#347;ci si&#281; w ramach pie&#347;ni ludowej; mog&#322;a wyj&#347;&#263; 
  tylko z r&#281;ki muzyka-fachowca: czy to rycerza-truwera czy te&#380; kantora, 
  kierownika choru, z przeznaczeniem jednak dla sfer rycerskich (а wi&#281;c kompozycja 
  dworska).&quot; Uczony ten wskazywa&#322; te&#380;, &#380;e pierwszy motyw melodyczny 
  (siedem nut nad s&#322;owem <em>Bogurodzica</em>) &quot;pokrywa si&#281; wiernie 
  nuta w nut&#281; z trzecim schematem <em>Litanii</em>, kt&oacute;rego najstarszy 
  zapis zachowany w Grazu pochodzi z XII wieku.&quot; Jednocze&#347;nie motyw 
  ten - pisa&#322; Feicht - jest wiernie zbie&#380;ny z pocz&#261;tkiem pie&#347;ni<em> 
  Par dessor l'ombre d'un bois</em>, truwera Jehana de Braine (zm. 1240). Pr&oacute;cz 
  tego jednego motywu, kt&oacute;ry by&#263; mo&#380;e by&#322; znany (popularny?) 
  w XIII-wiecznej Europie zar&oacute;wno w muzyce religijnej, jak i &#347;wieckiej, 
  w&#347;r&oacute;d trubadur&oacute;w i truwer&oacute;w - nie znaleziono dot&#261;d 
  &#380;adnego pierwowzoru muzycznego <em>Bogurodzicy</em>, со niew&#261;tpliwie 
  &#347;wiadczy о oryginalno&#347;ci tej pie&#347;ni. Nie znaleziono te&#380; 
  pierwowzoru tekstowego w pie&#347;niarskiej poezji &#322;aci&#324;skiej. Badacz 
  literacki tej pie&#347;ni, prof. Julian Krzy&#380;anowski, przypuszcza&#322;, 
  &#380;e powsta&#322;a ona w &#347;rodowisku dworskim z okazji koronacji W&#322;adys&#322;awa 
  Jagie&#322;&#322;y w 1386 roku i &quot;by&#322;a napisana j&#281;zykiem dla 
  nowego kr&oacute;la zrozumia&#322;ym&quot;. <br>
  <br>
  Do dnia dzisiejszego trwaj&#261; w&#347;r&oacute;d uczonych spory na temat powstania 
  <em>Bogurodzicy</em>. Przewa&#380;a jednak pogl&#261;d, i&#380; jest to oryginalny 
  utw&oacute;r polski, kt&oacute;ry powsta&#322; w XIV wieku. Na pocz&#261;tku 
  XV stulecia pe&#322;ni&#322; ju&#380; rol&#281; pobudki rycerskiej (&#347;piewanej 
  przed bitwami) oraz hymnu rycerskiego. <br>
  <br>
  Pisz&#261;c о bitwie pod Grunwaldem, Jan D&#322;ugosz zanotowa&#322; w XI ksi&#281;dze 
  <em>Rocznik&oacute;w, czyli Kronik s&#322;awnego Kr&oacute;lestwa Polskiego</em>, 
  &#380;e przed rozpocz&#281;ciem walki wojsko kr&oacute;lewskie za&#347;piewalo 
  <em>Bogurodzic&#281;</em>, kt&oacute;r&#261; XV-wieczny kronikarz okre&#347;li&#322; 
  jako &quot;patrium carmen&quot; (czyli &quot;pie&#347;&#324; ojc&oacute;w&quot;, 
  &quot;pie&#347;&#324; ojczyst&#261;&quot;). Jest to po ra&#380; pierwszy w naszych 
  dziejach u&#380;yte okre&#347;lenie pie&#347;ni narodowej pe&#322;ni&#261;cej 
  rol&#281; hymniczn&#261;. W tym wypadku - wzbudzaj&#261;cej poczucie jedno&#347;ci, 
  to&#380;samo&#347;ci w&#347;r&oacute;d rycerstwa. Ale ju&#380; w 1431 roku, 
  о czym te&#380; pisa&#322; D&#322;ugosz w <em>Kronikach</em>, pie&#347;&#324; 
  ta towarzyszy&#322;a ch&#322;opom kujawskim w zwyci&#281;skiej bitwie z Krzy&#380;akami 
  we wsi D&#261;bki ko&#322;o Nak&#322;a. Przez са&#322;у XV wiek <em>Bogurodzica</em> 
  by&#322;a te&#380; hymnem kr&oacute;lewskim dynastii Jagiellon&oacute;w, pe&#322;ni&#261;c 
  podobn&#261; rol&#281; jak poprzednio <em>Gaude, mater Poionia</em> w ko&#324;cowym 
  okresie panowania dynastii Piast&oacute;w. <br>
  <br>
  Po blisko 200 latach nik&#322;ego zainteresowania <em>Bogurodzic&#261;</em> 
  na pocz&#261;tku XIX wieku wydobyli j&#261; z zapomnienia autorzy wierszy patriotycznych, 
  cz&#281;sto odwo&#322;uj&#261;cy si&#281; w nich do tej pie&#347;ni. Wydaj&#261;c 
  w 1816 roku pami&#281;tne <em>&#346;piewy historyczne</em> - Jan Ursyn Niemcewicz 
  zamie&#347;ci&#322; t&#281; pie&#347;&#324; na ich pocz&#261;tku. Od tego czasu 
  <em>Bogurodzica</em> jako po&#322;&#261;czony symbol wiary religijnej i poczucia 
  patriotyzmu przewija si&#281; w tw&oacute;rczo&#347;ci prawie wszystkich naszych 
  najwybitniejszych poet&oacute;w i pisarzy. Poczynaj&#261;c od S&#322;owackiego, 
  kt&oacute;ry poetyck&#261; parafraz&#261; <em>Bogurodzicy</em> uczci&#322; wybuch 
  powstania listopadowego - ро Baczy&#324;skiego i Gajcego, tragicznych piewc&oacute;w 
  powstania warszawskiego, kt&oacute;rzy w sierpniu 1944 roku odwo&oacute;ywali 
  si&#281; w swych wierszach pisanych w ogniu do <em>Bogurodzicy</em>. <br>
