	<span class="header"><strong>Bogurodzica<br>
  (Богородица) </strong><br></span>
  <br>
  Bogurodzica dziewica, Bogiem s&#322;awiena Maryja. <br>
  U twego syna, Gospodzina, matko zwolena, Maryja! <br>
  Zyszczy nam, spu&#347;ci nam. <br>
  Kyrie eleison. <br>
  <br>
  Twego dziela Krzciciela, bo&#380;ycze, <br>
  Us&#322;ysz g&#322;osy, nape&#322;&#324; my&#347;li cz&#322;owiecze. <br>
  S&#322;ysz modlitw&#281;, j&#261;&#380; nosimy, <br>
  А da&#263; raczy, jeno&#380; prosimy: <br>
  А na &#347;wiecie zbo&#380;ny pobyt, <br>
  Po &#380;ywocie rajski przebyt. <br>
  Kyrie eleison.<br>
  <br>
  XIV wiek<br>
  <br>
  <br>
