  Авторы и время создания не установлены. 
  Мелодия (по крайней мере, современная мелодия, известная по меньшей мере с начала 1940-х гг.) напоминает мелодию песенки Шолома Секунды на слова Аншеля Шора<em> Mayn yidishe maydele </em>(&quot;Моя еврейская девушка&quot;). Песенка была написана для мюзикла Секунды <em>Zayn yidishe meydl</em> (&quot;Его еврейская девушка&quot;), поставленного в сентябре 1927 г. в Либерти Театре в Бруклине, Нью-Йорк. Сюжет мюзикла - любовь русского офицера Гриши и еврейской девушки Анюты накануне Первой мировой войны; женитьбе препятствует принадлежность возлюбленных к разным религиям, но офицер неожиданно узнает, что его мать была еврейкой. Они женятся по иудейскому обряду и уезжают в Палестину.<br>
  <br>
  Ноты<em> Mayn yidishe maydele</em> доступны на сайте <a href="https://www.loc.gov/item/ihas.200155217/" target="_blank">Библиотеки Конгресса США</a>. <br>
  <br>
  Можно  предположить, что мелодия песни Секунды повлияла на мелодию &quot;Москвы златоглавой&quot;, но  вероятно и обратное, так как Секунда использовал для создания экзотического колорита напевы, которые публика воспринимала как русские  (см. <a href="http://www.milkenarchive.org/works/view/585"  target="_blank">The Milken Archive</a>).<br>
  <br>
  Секунда родился в 1894 г. на Украине, затем его семья эмигрировала в США, где он стал композитором; широко 
  известен благодаря фокстроту &quot;Для меня ты самая красивая&quot;, на которую 
  в СССР сложена песня <a href="../dvor/vkejptaunskom.php">&quot;В Кейптаунском 
  порту&quot;</a>.<br><br> 
  &quot;Москва златоглавая&quot; была популярной песней
  русской эмиграции, входила в репертуар Алеши Дмитриевича. Использована в советском кинофильме 1980 г. &quot;Крах операции &quot;Террор&quot;, 
  режиссер Анатолий Бобровский; песню для фильма исполнила Елена Камбурова.<br>
    <br>
    <br>
    Устойчивое словосочетание &quot;Москва златоглавая&quot; бытовало еще до революции 1917 г. В частности, так назывался иллюстрированный деловой путеводитель по Москве, выпущенный в 1914 г. Е.В. Гзовской (Москва златоглавая. М.: Издание Е.В. Гзовской, 1914). Так же именовались несколько дешевых  песенников, вышедших перед Первой мировой войной, в частности:<br>
    <br> 
    <em>- Москва златоглавая: Сборник новейших песен. М.: Изд. Е.И. Коноваловой, 1907. 32 с. <br> 
   -  Москва златоглавая: Новейший сборник песен. М.: Изд. торг. дома Е. Коновалова и Ко, 1908. 24 с. <br>  
   - Москва златоглавая: Сборник песен. М.: Книгоизд-во Максимова, 1912. XVI с. <br>
    - Москва златоглавая: Сборник песен. М.: Книгоизд-во торг. дома Евдокия Коновалова и Ко, 1914. 24 с.</em><br>
    <br>
    Первый из указанных песенников открывается песней <a href="../popular20/moskvazl.php">&quot;Москва златоглавая (Новая песня)&quot; авторства Н.И. Красовского</a> (с. 5):<br>
    <em><br>
   Мы все в Москве живем не тужим,<br>
И, веселясь, свободы ждем,<br>
И честно Бахусу мы служим,<br>
Да яро монопольку пьем.<br>
    <div style="margin-left:16px">Ах Москва, Москва, Москва,</div>
		<div style="margin-left:32px">Золотая голова.</div>
