		<div style="margin-left:32px">Белокаменная!</div></em>
        <br>
        Песней &quot;Москва златоглавая&quot;, с тем же припевом, но совершенно другими куплетами, без указания автора, открывается и второй песенник (с. 3-4):<br>
        <br>
       <em> Вдали тебя я обездолен,<br>
