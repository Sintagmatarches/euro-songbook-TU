	<div style="margin-left:16px">Белокаменная!</div></em>
                <br>
        Эту же версию песни содержат два остальных песенника (1912 и 1914 гг. - стр. VII-VIII и 3-4 соответственно), но там песня называется <a href="../popular20/moskvamoskva.php">&quot;Москва, Москва&quot;</a> (таким образом, песни с заглавием &quot;Москва златоглавая&quot; в них нет). 
          Ни одна из песен из указанных четырех песенников не имеет ничего общего с текстом &quot;эмигрантской&quot; песни.<br>
