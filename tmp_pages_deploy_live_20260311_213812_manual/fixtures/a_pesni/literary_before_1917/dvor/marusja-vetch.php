  <span class='bold'>ВАРИАНТ<br>
  <br>
  Ой, вечер вечереет...<br></span>
  <br>
  Ой, вечер вечереет,<br>
  Колышется трава.<br>
  Нейдёт, нейдёт мой милый,<br>
  Пойду к нему сама.<br>
  <br>
  Иду я первым залом<br>
   И вижу пред собой:<br>
  Мой милый на коленях<br>
  Клянётся пред другой.<br>
  <br>
  Она его пытает:<br>
  - С другою есть любовь?<br>
   А он ей отвечает,<br>
  Что нету никакой.<br>
  <br>
  Я вышла на крылечко<br>
  И больше не могу.<br>
  Он держит на коленях<br>
  Соперницу мою.<br>
  <br>
  Подружка ты, подружка,<br>
  Соперница моя,<br>
  На что отбила друга?<br>
  Люби, страдай, как я.<br>
  <br>
  Огонь горит, пылает,<br>
  Любовь ещё сильней.<br>
  Огонь зальёшь водою,<br>
  Любовь нельзя ничем.<br>
  <br>
  Ой, вечер вечереет,<br>
  Подружки в сад идут,<br>
  Маруся отравилсь,<br>
  В больницу её везут.<br>
  <br>
  В больнице две сестрицы,<br>
  Два доктора сидят.<br>
  Берут её на руки,<br>
  Хотят её спасать.<br>
  <br>
  - Спасайте - не спасайте,<br>
  Мне жизнь не дорога.<br>
  Я с миленьким рассталась,<br>
  Отравы приняла.<br>
  <br>
  Призодят к ней подружки<br>
Марусю навестить,<br>
А доктор отвечает:<br>
- Без памяти лежит.<br>
<br>
Пришёл еее любезный<br>
Марусю навестить,<br>
А доктор отвечает:<br>
- В часовне уж лежит.<br>
<br>
Приходит он в часовню,<br>
Там чёрный гроб стоит.<br>
Открыл он в гробе крышку,<br>
Маруся в нём лежит.<br>
<br>
- Маруся ты, Маруся,<br>
Открой свои глаза!<br>
А если не откроешь,<br>
Помру с тобой и я.<br>
<br>
Ой, вечер вечереет,<br>
Все с фабрики идут,<br>
А бедную Марусю<br>
На кладбище несут.<br>
<br>
Вот гроб её черненый,<br>
Священник впереди.<br>
Подружки дорогие<br>
Кричат: &quot;Прости, прости!&quot;<br>
