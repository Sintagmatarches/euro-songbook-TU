	<h1>МАРУСЯ </h1>
  <br>
  Вечер вечереет,<br>
  Работницы идут.<br>
  Маруся отравилась –<br>
  В больницу повезут.<br>
  <br>
  В больницу привезли ее<br>
  И клали на кровать.<br>
  Два доктора с сестрицей <br>
  Старались жизнь спасать.<br>
  <br>
  «Спасайте не спасайте,<br>
  Мне жизнь не дорога:<br>
  Я милого любила,<br>
  Такого подлеца».<br>
  <br>
  Давали ей лекарства –<br>
  Она их не пила,<br>
  Давали ей таблетки –<br>
  Она их не брала.<br>
  <br>
  Приходит к ней мамаша<br>
  Свою дочь навестить.<br>
  А доктор отвечает:<br>
  «При смерти она лежит».<br>
  <br>
  Приходит к ней любезный,<br>
  Марусю навестить.<br>
  А сторож отвечает:<br>
  «В часовне она лежит».<br>
  <br>
  В часовню он заходит,<br>
  Там белый гроб стоит,<br>
  А в том белом гробе<br>
  Марусенька лежит.<br>
  <br>
  «Маруся ты, Маруся,<br>
  Открой свои глаза!<br>
  А если не откроешь –<br>
  Умру с тобой и я».<br>
  <br>
  Но вот уже и время,<br>
  Пришла машина тут.<br>
  И бедную Марусю<br>
  На кладбище везут.<br>
  <br>
  А милый идет сзади,<br>
  Кричит: «Прости, прощай!<br>
  Прости, прощай, Маруся!<br>
  Прости, прости меня!<br>
  Я знаю, что Маруся<br>
  З-за меня умерла».<br>
  <br>
  А вечер вечереет,<br>
  Работницы идут,<br>
  А бедную Марусю<br>
  На кладбище везут.<br>
  <br>
        <span class='i'>Песни нашего двора / Авт.-сост. Н.В. Белов. Минск: Современный литератор, 
        2003.  (Золотая коллекция). </span><br>
