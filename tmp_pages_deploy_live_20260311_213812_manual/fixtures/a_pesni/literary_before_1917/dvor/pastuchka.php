	<h1>ПАСТУШКА</h1>
  <br>
  По деревне ходила со стадом овец<br>
  Деревенская Катя-пастушка.<br>
  И понравился ей укротитель зверей<br>
  Чернобровый красавец Андрюшка.<br>
  <br>
  - Переменим с тобой деревенскую жизнь<br>
  На разгульную жизнь городскую,<br>
  Снаряжу я тебя в темно-синий костюм,<br>
  Сам одену я шляпу большую.<br>
  <br>
  Вот уж год как прошел, а Андрюши все нет,<br>
  И ребенок родился к тому же.<br>
  Катя сильно грустна, взяв ребенка, она<br>
  Едет в город разыскивать мужа.<br>
  <br>
  Научилась она водку горькую пить,<br>
  Тут ребенок при этом скончался.<br>
  Научилась она в рестораны ходить,<br>
  Наконец ей Андрюша попался.<br>
  <br>
  - Здравствуй, миленький мой, муженек дорогой,<br>
  Как давно по тебе я скучаю.<br>
  А Андрюша-подлец покачал головой:<br>
  - Не видал я тебя и не знаю.<br>
  <br>
  Закипела тут кровь в молодецкой груди,<br>
  Финский нож ему в сердце вонзила:<br>
  - За измену твою, за коварную жизнь,<br>
  Тут за все я тебе отомстила!<br>
  <br>
  <span class='i'>В нашу гавань заходили корабли. Пермь: Книга, 1996. 
  Перепечатано: В нашу гавань заходили корабли. Вып. 2. М.: Стрекоза, 2000. Этот 
  же вариант: фонограмма Веры Алентовой, CD «В нашу гавань заходили корабли» № 
  1, «Восток», 2001.</span><br>
 <br>
  <br>
  Более привычное название - &quot;Катя-пастушка&quot;. Популярный жестокий романс. Заимствован русскими цыганами, у которых  превратился 
  в песню &quot;По деревне с кнутом&quot; (на собственную мелодию), 
  где Андрюша стал более положительным персонажем: Катю не обманывал, уехал вместе 
  с ней в город, но там спился.<br>
  <br>
  <br>
  Далее из книги: Сказки и песни, рожденные в дороге: Цыганский 
  фольклор / Сост., запись, пер. с цыганск., предисл. и коммент. Е. Друца и А. 
  Гесслера. М.: Наука, Главн. ред. восточной литературы, 1985. С. 396-397, 486-487: <br>
  <br>
 <em>121. По деревне с кнутом</em></strong><em><br>
  <br>
  По деревне с кнутом<br>
  Гонит стадо коней<br>
  Чернобровый красавец Андрюша. <br>
  <br>
  «Ах, Андрей, ты, Андрей,<br>
  Не пугай, брат, гусей», -<br>
  Говорит ему Катя-пастушка. <br>
  <br>
  Очень нравился ей<br>
  Укротитель коней.<br>
  Парень он городской был по виду. <br>
  <br>
  «Я тебе подарю<br>
  Темно-синий костюм<br>
  И куплю тебе шляпу с полями». <br>
  <br>
  Изменили они<br>
  Свою прежнюю жизнь<br>
  Деревенскую на городскую. <br>
  <br>
  Стал он пить, стал кутить,<br>
  По шалманам ходить.<br>
  Доля горькая Кате досталась. <br>
  <br>
  Песня записана в Антропшино Ленинградской обл. с напева В. А. Ивановой, 1926 
  г. рожд. Нами записаны также многочисленные варианты этой песни, в частности 
  в Лианозово Московской обл., с напева Т. Николаевой и многие другие варианты. 
  <br>
  <br>
  У песни есть вполне определенный первоисточник в русском фольклоре. Мы имеем 
  в виду не менее популярную, чем у цыган, русскую песню «Катя-пастушка», относящуюся 
  к числу так называемых «жестоких» романсов сельского происхождения. Приводим 
  весь текст этой песни, чтобы дать возможность сравнить его с цыганским и показать 
  определенные тенденции в цыганском фольклоре. Вот этот текст: <br>
  <br>
  По деревне гнала она стадо овец,<br>
  Деревенская Катя-пастушка,<br>
  И понравился ей покоритель сердец –<br>
  Чернобровый красавец Андрюшка. <br>
  <br>
  «Давай, Катя, с тобой переменим мы жизнь<br>
  Деревенскую на городскую,<br>
  Разодену тебя в шелки, бархат, атлас<br>
  И куплю тебе шаль голубую». <br>
  <br>
  Вот год на исход, и Андрея все нет –<br>
  Позабыл деревенскую Катю.<br>
  И ребенка взяла, проклиная судьбу,<br>
  Пошла в город искать там Андрея. <br>
  <br>
  Научилась она водку горькую пить.<br>
  В это время ребенок скончался.<br>
  Научилась она по театрам ходить.<br>
  В это время Андрей повстречался. <br>
  <br>
  «Здравствуй, мой Андрей, здравствуй, мой дорогой,<br>
  Сколько лет я с тобой не встречалась!»<br>
  Кучерявый подлец покачал головой:<br>
  «Я тебя не видал и не знаю». <br>
  <br>
  Закипела в ней кровь в груди молодой,<br>
  И кинжал она в сердце вонзила.<br>
  «За измену твою, за неправду твою<br>
  И за сына тебе отомстила». <br>
  <br>
  Этот вариант песни записан Юрием Бойко с напева южнорусского хора. <br>
  <br>
  Следует сказать, что при очевидном заимствовании текста песни цыгане в значительной 
  степени сместили в нем смысловые акценты. Кроме того, мелодии песни в цыганском 
  и русском вариантах не имеют решительно никакого сходства. Последнее часто можно 
  встретить в цыганском фольклоре – заимствование текста и присочинение оригинальной 
  мелодии. В качестве иллюстрации можно привести песню «Шэл мэ вэрсты» («Сотни 
  верст я, молодец, прошел…», № 198), а также многие другие песни.</em><br>
