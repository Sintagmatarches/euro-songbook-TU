  <br>
  Вот прошел уже год, а Андрея все нет,<br>
  Он не едет за Катей в деревню.<br>
  Взяв малютку с собой, проклинав белый свет,<br>
  Едет в город разыскивать батьку.<br>
  <br>
  Научилась она водку горькую пить,<br>
  И ребенок у ней тут скончался.<br>
  Научилась она по бульварам ходить,<br>
  И Андрей ей навстречу попался.<br>
  <br>
  - Здравствуй, миленький мой, муженек дорогой,<br>
  Так давно я тебя не видала!<br>
