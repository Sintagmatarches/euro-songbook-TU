  <br>
  В деревенском саду<br>
  Пасла стадо овец<br>
  Деревенская Катя-пастушка.<br>
  Понравился ей покоритель сердец –<br>
  Молодой и красивый Андрюшка. <br>
  <br>
  Давай, Катя, с тобой<br>
  Переменим мы жизнь<br>
  Деревенскую на городскую.<br>
  Разодену тебя в шелка, бархат, атлас<br>
  И куплю тебе шаль голубую. <br>
  <br>
  Разодел он ее<br>
  В шелка, бархат, атлас,<br>
  И купил он ей шаль голубую.<br>
  Насмеялся над ней покоритель сердец,<br>
  Сам уехал в сторонку чужую. <br>
  <br>
  <br>
  И с ребенком в руках, <br>
  Проклиная судьбу,<br>
  Катя едет разыскивать мужа. <br>
  <br>
  Научилась она водку горькую пить,<br>
  А затем научила и сына.<br>
  Научилась она по бульварам ходить,<br>
  Где и встретила мужа Андрея. <br>
  <br>
  «Здравствуй, муж дорогой,<br>
  Ты ребенку отец.<br>
  Ах, как долго тебя не видала!<br>
  Посмотри, на руках твой красавец сынок,<br>
  В честь тебя я Андреем назвала». <br>
  <br>
  «Уходи от меня,<br>
  Ненавижу тебя,<br>
  Ненавижу тебя, проклинаю,<br>
  А ребенка того, что у тебя на руках,<br>
  Я за сына своего не считаю». <br>
  <br>
  Разгорелась в душе<br>
  Материнская боль.<br>
  Катя ножик Андрею вонзила:<br>
  «Это, милый, тебе за измену твою.<br>
  Я за сына тебе отомстила». <br>
  <br>
  Не пасет больше стад<br>
  В деревенском саду<br>
  Деревенская Катя-пастушка.<br>
  Разонравился ей покоритель сердец –<br>
  Молодой и красивый Андрюшка.<br>
  <br>
  <span class='i'>Песни нашего двора / Авт.-сост. Н.В. Белов. Минск: Современный 
  литератор, 2003. (Золотая коллекция). </span><br>
