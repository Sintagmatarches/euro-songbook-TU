  Далеко за селом пасла стадо овец<br>
  Деревенская Катя-пастушка.<br>
  И понравился ей покоритель сердец<br>
  Кучерявый красавец Андрюшка.<br>
  <br>
  Давай, Катя, с тобой поменяем мы жизнь<br>
  Деревенскую на городскую.<br>
  Разодену тебя в бархат, шёлк и атлас,<br>
  И куплю тебе шаль голубую.<br>
  <br>
  Вот уж годик прошёл, и другой настает,<br>
  Начинается зимняя стужа.<br>
  Катя сына берёт и по свету идёт,<br>
  Катя едет разыскивать мужа.<br>
  <br>
  Научилась она в рестораны ходить,<br>
  И ни в чём тут себя не жалея,<br>
  Научилась она водку горькую пить,<br>
  Тут и встретила мужа Андрея.<br>
  <br>
  - Здравствуй, милый ты мой, муженёк дорогой,<br>
  Как я долгоя тебя не видала.<br>
  Посмотри на него, сын красавец растёт,<br>
  Твоим именем сына назвала.<br>
  <br>
  Поглядел на нее, покачал головой:<br>
  - Не видал я тебя и не знаю.<br>
  А про сына того, что ты мне говоришь,<br>
  Своим сыном его не считаю.<br>
  <br>
  Заблестел острый нож, засверкали глаза,<br>
  И кинжал ему в сердце вонзила.<br>
  - За измену твою, за неправду твою,<br>
  И за всё я тебе отомстила.<br>
