Слова и музыка Александра Вертинского<br>
<br>
В пыльный маленький город, где вы жили ребенком,<br>
Из Парижа весной к вам пришел туалет.<br>
В этом платье печальном вы казались Орленком,<br>
Бледным маленьким герцогом сказочных лет.<br>
<br>
В этом городе сонном вы вечно мечтали<br>
О балах, о пажах, вереницах карет<br>
И о том, как ночами в горящем Версале<br>
С мертвым принцем танцуете вы менуэт.<br>
<br>
В этом городе сонном балов не бывало,<br>
Даже не было просто приличных карет.<br>
Шли года… Вы поблекли, и платье увяло,<br>
Ваше дивное платье «Мезон Лавалетт».<br>
<br>
Но однажды сбылися мечты сумасшедшие.<br>
Платье было надето. Фиалки цвели.<br>
И какие-то люди, за нами пришедшие,<br>
В катафалке по городу вас повезли.<br>
<br>
На слепых лошадях колыхались плюмажики,<br>
Старый попик любезно кадилом махал, -<br>
Так весной в бутафорском смешном экипажике<br>
Вы поехали к Богу на бал.<br>
