          <strong>Дроздовцы</strong><br>
          <br>
          Основа дроздовских частей - <strong>Отряд полковника Дроздовского</strong> 
          (ок. 1000 человек разных родов войск), сформированный <a href="../foto/drozdovskij.php">Михаилом 
          Гордеевичем Дроздовским</a> (1881-1919) из добровольцев на Румынском 
          фронте. 11 марта отряд выступил в поход Яссы-Дон на соединение с Добровольческой 
          армией, о чем и поется в &quot;Марше дроздовцев&quot;. В Добровольческой 
          армии они составили 3-ю пехотную дивизию. <br>
          <br>
          Стрелковый полк Отряда, в начале мая 1918 в Новочеркасске преобразованный 
          в Офицерский полк, при соединении с Добровольческой армией получил наименование 
          2-го офицерского, после смерти генерал-майора Дроздовского получил его 
          имя, и с 4 января 1919 именовался 2-й офицерский стрелковый генерала 
          Дроздовского полк (с 22 августа 1919 1-й офицерский стрелковый генерала 
          Дроздовского полк, с апреля 1920 1-й стрелковый генерала Дроздовского 
          полк; неофициально - 1-й Дроздовский полк). 27 июля 1919 на базе офицерского 
          кадра Дроздовского полка был сформирован 4-й офицерский полк (с 22 августа 
          2-й офицерский стрелковый генерала Дроздовского полк, с апреля 1920 
          2-й стрелковый генерала Дроздовского полк; неофициально - 2-й Дроздовский 
          полк). <br>
          <br>
          Пришедший с Дроздовским из Румынии Конный дивизион ротмистра Гаевского 
          29 апреля 1918 переформирован в Конный полк, с 31 мая – 2-й офицерский 
          конный полк, с 10 октября 1919 2-й конный генерала Дроздовского полк. 
          <br>
          <br>
          Батареи Отряда (3-я отдельная легкая и Гаубичная) составили 3-й отдельный 
          легкий артиллерийский дивизион, на базе которого 4 апреля 1919 сформирована 
          3-я артиллерийская бригада (с 22 октября 1919 – Дроздовская артиллерийская 
          бригада). <br>
          <br>
          14 октября 1919 3-я дивизия преобразована в Дроздовскую дивизию. После 
          эвакуации Русской Армии из Крыма в Галлиполи полки и конный дивизион 
          Дроздовской дивизии сведены в Дроздовский стрелковый полк (официальное 
          название - Сводно-стрелковый генерала Дроздовского полк, затем - 2-й 
          офицерский стрелковый генерала Дроздовского полк).<br>
