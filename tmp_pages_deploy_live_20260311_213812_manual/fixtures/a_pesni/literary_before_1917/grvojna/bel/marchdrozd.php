		  <br>
          По воспоминаниям Парфенова, опубликованных им в журнале &quot;Красноармеец 
          - Краснофлотец&quot; незадолго до ареста (в №21 за 1934 год), партизанскую 
          &quot;По долинам, по загорьям&quot; он сложил на мотив своих же ранних 
          песен &quot;На Сучане&quot;, созданных в июле 1914 года:<br>
          <br>
          <em>По долинам и по взгорьям<br>
          Целый месяц я бродил,<br>
          Был на реках и на взморьях<br>
          Не жалея юных сил.</em><br>
          <br>
          Выходит, это и есть первоисточник всех последующих песен: и партизанской, 
          и дроздовской, и песни сибирских стрелков. Неизвестно только, сам ли 
          Парфенов сочинил ее мотив, или, в свою очередь, где-то заимствовал.<br>
          <br>
          С этой версией согласуется и комментарий к CD «Тернистый путь борьбы 
          и муки. Песни Белого движения и Русского Зарубежья» (СПб., IMLab, 2004, 
          автор комментариев Константин Никитин), где происхождение дроздовского 
          марша излагается так:<br>
