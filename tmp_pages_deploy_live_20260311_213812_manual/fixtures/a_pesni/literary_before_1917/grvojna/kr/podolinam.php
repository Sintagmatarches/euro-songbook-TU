        <strong>Родственные песни </strong><br>
        <br>
        На этот же мотив поется песня сибирских стрелков Первой мировой войны 
        <a href="../../ww1/iz_tajgi.php">&quot;Из тайги, тайги дремучей&quot;</a> 
        на стихотворение Владимира Гиляровского, написанное в 1915 году, и белогвардейский 
        <a href="../bel/marchdrozd.php">&quot;Марш дроздовцев&quot;</a>, созданный 
        в июне 1919 года на Украине. <br>
        <br>
        Песни Парфенова &quot;На Сучане&quot; (По долинам и по взгорьям целый 
        месяц я бродил...), от которых &quot;Партизанская&quot; получила мелодию 
        и первую строку, написаны, как вспоминает автор, в июле 1914 года. То 
        есть, это самые ранние песни на этот мотив из нам известных. Если мотив 
        сочинен самим Парфеновым, то &quot;На Сучане&quot; и есть первоисточник 
        мелодии. <br>
        <br>
        Об этом же косвенно утверждается в комментариях к песням CD «Тернистый 
        путь борьбы и муки. Песни Белого движения и Русского Зарубежья» (СПб., 
        INLab, 2004, автор комментариев - солист хора &quot;Валаам&quot; Константин 
        Никитин):<br>
        <br>
        <em>&quot;Чрезвычайно любопытна история Марша Дроздовского полка «Из Румынии 
        походом…». С его мелодией полностью совпадает мелодия большевистской песни 
        «По долинам и по взгорьям». Однако последняя была записана руководителем 
        Ансамбля красноармейской песни А. В. Александровым лишь в 1929 г. В действительности, 
        «Марш Дроздовского полка» (на слова П. Баторина) «был заказан полковником 
        А. В. Туркулом композитору Дмитрию Покрассу в Харькове 27 июня 1919 года, 
        а исполнена уже 29 июня, возможно, в присутствии главнокомандующего, генерала 
        А. И. Деникина, на банкете по случаю занятия города Белыми» [газета «Новое 
        русское слово» (США), 6 и 14 декабря 1974 г.]. <strong>Основная часть 
        мелодии марша была заимствована Дм. Покрассом из дореволюционной песни 
        дальневосточных охотников «По долинам, по загорьям»</strong>; отдельные 
        же обороты сходны с украинской песней «Розпрягайте, хлопцi, коней». Есть 
        предположение, что мотив песни был сочинен добровольцами генерала Чернявского 
        еще в 1828 г.&quot;</em><br>
        <br>
        Кстати, Петр Парфенов в 1914-1916 годах воевал как раз на Румынском фронте, 
        вместе с будущими дроздовцами. Наверняка он исполнял на фронте свои песни 
        - точно так же, как потом делал это в ходе Гражданской войны, и сам таким образом заложил основу для дроздовского марша. <br>
