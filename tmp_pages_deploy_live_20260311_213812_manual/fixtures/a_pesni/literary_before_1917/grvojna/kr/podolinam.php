        <br>
        1920, окончательная редакция - 1922<br>
        <br>
        <br>
        Другие названия - &quot;Партизанская&quot;, &quot;Партизанский гимн&quot;. 
        Автор текста - участник Гражданской войны в Сибири и Приморье, писатель, 
        подпольщик, партизан и красный командир Петр Парфенов (родился в 1894 
        году в Уфимской губернии, в 1935-м арестован, согласно свидетельству о 
        смерти умер в 1943 году от воспаления легких).<br>
        <br>
        Историю создания песни он успел изложить незадолго до ареста в журналах 
        &quot;Красноармеец и краснофлотец&quot; (1934, №21) и &quot;Музыкальная 
        самодеятельность&quot; (1935, №10).<br>
        <br>
        Парфенов пишет, что в феврале 1919 года он, по просьбе своего друга Ефима 
        Мамонтова, собиравшего партизанские отряды на Алтае, создал подпольную 
        агитационную песню &quot;Наше знамя&quot; (дело происходило в Барнауле, 
        где Парфенов лежал в госпитале):<br>
        <br>
        <em>Мы землеробы<br>
        Будем вольно<br>
        В родной Сибири нашей жить.<br>
        И не дадим свое приволье<br>
        Ни отменить,<br>
        Ни изменить.</em><br>
        <br>
        Написал он ее &quot;на проверенную с музыкальной стороны мелодию своих 
        ранних песен “На Сучане” от 10 июля 1914 года:<br>
        <em><br>
        По долинам и по взгорьям<br>
        Целый месяц я бродил,<br>
        Был на реках и на взморьях<br>
        Не жалея юных сил&quot;.</em><br>
        <br>
        Из этой фразы Парфенова непонятно, откуда взялся мотив: сам ли он его 
        подобрал в 1914 году, или заимствовал.<br>
        <br>
        Выйдя из барнаульского госпиталя, Парфенов внедрился в колчаковскую контрразведку 
        г. Камень-на-Оби и под видом служебных разъездов готовил восстание окрестных 
        уездов. Его песня между тем получила популярность в среде мобилизованных 
        на Алтае колчаковцев, у алтайских и енисейских партизан. <br>
        <br>
        Затем Парфенов перебирался на Дальний Восток. Весной 1920 года он исполнил 
        &quot;Наше знамя&quot; в Народном доме Владивостока на торжественном заседании, 
        посвященном освобождению города и, как тогда полагали, окончанию Гражданской 
        войны.<br>
        <br>
        Песня понравилась, и военный совет Приморья поручил Парфенову написать 
        новые слова, чтобы восполнить нехватку красноармейских песен. Тогда Парфенов 
        и создал первый вариант &quot;По долинам, по загорьям&quot;, назвав его 
        &quot;Партизанским гимном&quot;. Но текст получился &quot;сырым&quot; 
        и для распространения непригодным:<br>
        <br>
        <em>По долинам, по загорьям<br>
        Шли дивизии вперед,<br>
        Чтобы с боем взять Приморье —<br>
        Белой армии оплот.<br>
        <br>
        Чтобы выгнать интервентов<br>
        За рубеж родной страны.<br>
        И не гнуть пред их агентом<br>
        Трудовой своей спины.<br>
        <br>
        Становились под знамена,<br>
        Создавали ратный стан<br>
        Удалые эскадроны<br>
        Приамурских партизан.<br>
        <br>
        Этих дней не смолкнет слава<br>
        Не забудут никогда<br>
        Как лихая наша лава<br>
        Занимала города.<br>
        <br>
        Сохранятся, точно в сказке<br>
        Вековые будто пни<br>
        Штурмовые ночи Спасска,<br>
        Николаевские дни.<br>
        <br>
        Как мы гнали атаманов,<br>
        Как громили мы господ.<br>
        И на Тихом океане<br>
        Свой закончили поход.</em><br>
        <br>
        Парфенов принялся редактировать песню, но работа продвигалась медленно. 
        В ночь на 5-е августа японцы неожиданно для красных вторглись в Приморье 
        и захватили его, а текст так песни и не был закончен и опубликован. Однако 
        Парфенов, как он сам пишет, исполнял отредактированную песню &quot;довольно 
        часто&quot; на вечерах и собраниях. Так что в устной форме она бытовала 
        у приамурских партизан.<br>
        <br>
        Последние изменения в текст Парфенов внес в феврале 1922 года после разгрома 
        японцев и белогвардейцев на станции Волочаевская. Тогда же он добавил 
        посвящение &quot;светлой памяти Сергея Лазо, сожженного японо-белогвардейцами 
        в паровозной топке&quot;, а в марте 1922 года покинул Дальний Восток. 
        Опубликовать песню там он не успел. Приехав в Москву, он принес текст 
        в редакцию &quot;Молодая гвардия&quot;, но получил отказ, мотивированный 
        официальным курсом на &quot;борьбу с партизанщиной&quot;.<br>
        <br>
        В 1929 году в местечке Дарница под Киевом руководитель ансамбля красноармейской 
        песни Александр Александров услышал &quot;По долинам, по загорьям&quot; 
        от командира роты 134-го полка Украинского военного округа Ильи Атурова. 
        Поэт Сергей Алымов обработал текст, и песня в 1929 году вошла в репертуара 
        ансамбля Александрова с указанием авторства Атурова и Алымова.<br>
        <br>
        Это вызвало возмущение участников Гражданской войны в Приморье. Они заявляли, 
        что автор - Парфенов, и в 1934 году опубликовали об этом коллективную 
        статью в газете &quot;Известия&quot;. Следом сам Парфенов выступил в журналах 
        с историей создания песни, но из-за последующего ареста &quot;По долинам 
        и по взгорьям&quot; так и выходила с фальшивым авторством, пока в 1962 
        году авторство Парфенова не подтвердил суд. <br>
        <br>
        Подробнее о судьбе Петра Парфенова и истории песни см. Анатолий Муравлев. 
        <a href="a-podolinam.php">Судьба автора популярной песни</a> (&quot;Сибирские 
        огни&quot;, 2007, №12).<br>
        <br>
        <br>
        Благодаря ансамблю Александрова песня получила широкое распространение 
        в СССР и в мире, ее до сих пор поют антифашисты и революционеры разных 
        стран, вплоть до никарагуанских повстанцев. Она же послужила основой французской 
        анархической песни <a href="../../intern/makhnovtchina.php">&quot;Махновщина&quot;</a> 
        (есть также ее <a href="../../intern/machno-pl.php">польский вариант</a> 
        и вариант <a href="../../esperanto/mahnovscino.php">на эсперанто</a> с 
        <a href="../../esperanto/mahnovscino.mp3">mp3</a>). <br>
