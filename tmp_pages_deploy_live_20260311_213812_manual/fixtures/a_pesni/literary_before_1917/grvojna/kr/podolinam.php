        В апреле 1920 года в Прибайкалье была образована подконтрольная Советской 
        России буферная Дальневосточная республика (ДВР) с элементами демократии: 
        Учредительным собранием из большевиков, меньшевиков и эсеров, рыночной 
        экономикой и определенной свободой печати. Ключевые посты контролировались 
        большевиками, а вооруженные силы Республики - Народно-революционная армия 
        Василия Блюхера - фактически входили в состав Красной Армии, хотя формально 
        считались самостоятельным формированием. <br>
        <br>
        В 1920 году войска ДВР выбили белых и японцев из Читы и Забайкалья, а 
        в октябре 1922 года вошли в Приморье. К ноябрю силами партизан и Народно-революционной 
        армии белые и японские войска в Приморье были ликвидированы. Через несколько 
        дней, 15 ноября 1922 года, Дальневосточная республика была упразднена 
        и ее территория объявлена «нераздельной составной частью» Советской России. 
        Члены разрешенных в ДВР партий эсеров и меньшевиков арестованы. Блюхер 
        арестован в 1937 году и умер от пыток.<br>
        <br>
        С занятием Приморья в ноябре 1922 года Гражданская война в основном завершилась. 
        Еще несколько лет очаги сопротивления сохранялись в Средней Азии (басмачество 
        - до 1933 года) и в Закавказье. Последним крупным выступлением против 
        власти большевиков было национальное восстание в Грузии в августе 1924 
        года.<br>
