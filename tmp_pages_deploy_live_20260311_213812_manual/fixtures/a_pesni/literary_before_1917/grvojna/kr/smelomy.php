<br> 
<em>Одна из ранних песен эпохи гражданской войны (записано А.М. Новиковой в 20-е гг. в Тульской области). <br>
<br>
        Русские народные песни. Вступ. статья, сост. и примеч. А.М. Новиковой. М., Государственное издательство художественной литературы, 1957. С. 545-546. Без заглавия. </em><br>
<br>
<br>
        Одна из первых популярных красных песен Гражданской войны. Упоминается 
        в военных мемуарах, в романе Константина Федина «Необыкновенное лето» 
        (место действия – Саратов, 1919) и у Дмитрия Фурманова в «Чапаеве» (наступление 
        на Уфу, май 1919): «Слова тут пелись ничего не значащие, хорошая 
        песня еще не появилась, но припев… припев пели удивительно!». То есть, 
        песня устоялась где-то к середине 1919 года. <br>
<br>
Создана на основе армейской песни времен Первой мировой <a href="../../ww1/sldedy.php">«Слыхали, 
деды, война началася»</a> - мелодия ее куплетов восходит к романсу 1902 года <a href="../../romans/belakacii.php">«Белой 
акации гроздья душистые»</a> (разумеется, не из фильма &quot;Дни Турбиных&quot; 
- <a href="../../romans/basner/celnotch.htm">&quot;Целую ночь соловей нам насвистывал...&quot;</a> 
- это другой романс), мелодия припева - гусарская мазурка. <br>
<br>
Есть много вариантов куплетов, а также аналогичная <a href="../bel/slychalidedy.php">белая 
песня</a>. По фольклорной записи 
известна также красная песня <a href="sldedy-gr.php">&quot;Слыхали, деды, война 
началася&quot;</a> со старым припевом - времен Первой мировой. <br>
<br>
Припев &quot;Смело мы в бой пойдем&quot; в дальнейшем использовался и в других 
песнях - в частности, он закрепился в песне февральской революции 1917 года <a href="../../starrev/tchutzarja.htm">&quot;Чуть 
заря занялась над Россией&quot;</a> (первоначально она не имела припева).<br>
<br>
        В 1930-31 
        гг. Российская ассоциация пролетарских музыкантов (РАПМ) призвала запретить 
        &quot;Смело мы в бой пойдем&quot; как &quot;перелицовку старого буржуазного 
        песенного хлама&quot;. Сюда же попали <a href="marchbudennogo.php">&quot;Марш 
        Буденного&quot;</a> (как обработка еврейской свадебной мелодии), <a href="../../starrev/mykuznetsy.htm">&quot;Мы 
        кузнецы&quot;</a> (мотив венской шансонетки), <a href="../../drugije/molgvard.htm">&quot;Молодая 
        гвардия&quot;</a> (отзвуки старого национального гимна) и еще ряд песен. 
        Причем, если для &quot;Смело мы в бой пойдем&quot; происхождение от романса 
        не вызывает сомнений, с &quot;Марш Буденного&quot; тоже более-менее ясно 
        (вполне возможно, еврейская мелодия), но обвинения в адрес других песен 
        натянуты. Песни, разумеется, не были запрещены.<br>
