<br>
  <br>
  Обычно песня называется просто &quot;Каховка&quot;. По официальной советской 
  версии истории, возле селения Каховка на Днепре под Херсоном частям Красной 
  Армии (латышские стрелки) в августе 1920 года удалось остановить летнее наступление 
  Врангеля. <br>
  <br>
        В фильме песню исполнил актер Николай Баталов и в ноябре 1936 года записал 
        на пластинку (это единственная его запись на пластинку, вскоре он умер). 
        Это стихотворение, как и &quot;Гренада&quot;, оказалось пророческим - 
        в годы Второй мировой войны под Каховкой шли жестокие бои, и песня была 
        очень популярной у солдат. Когда Светлов приезжал на передовую, бойцы 
        недоумевали, почему автора &quot;Каховки&quot; и &quot;Гренады&quot; отпустили 
        на фронт - он же может погибнуть! В 1943 году Светлов написал новый текст 
        &quot;Каховки&quot; (<a href="../../ww2/oficial/kahovka.php">&quot;Украинский 
        ветер шумит над полками...&quot;</a>):<br>
  <br>
  <em>...Пожары легли над Каховкой родимой,<br>
  Кровава осенняя мгла,<br>
  И песни не слышно, и в сердце любимой<br>
  Немецкая пуля вошла...</em><br>
  <br>
  Песню исполял также Леонид Утесов (с измененной ст. 3: <em>&quot;Иркутск и Липава, 
  Каховка, Варшава&quot;</em>). <br>
  <br>
        Интересный момент. У песен <a href="zelezniak.php">&quot;Партизан Железняк&quot;</a> 
        (музыка Матвея Блантера, слова Михаила Голодного, 1935), &quot;Песня о Каховке&quot; 
        (музыка Исаака Дунаевского, слова Михаила Светлова, 1936) и <a href="orlenok.php">&quot;Орленок&quot;</a> 
        (музыка Виктора Белого, слова Якова Шведова, 1936) одинаковая мелодия и манера исполнения, 
