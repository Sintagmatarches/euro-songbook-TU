	<span class="header"><strong>ПАРТИЗАН 
  ЖЕЛЕЗНЯК</strong> <br></span>
  <br>
  Слова Михаила Голодного<br>
  <br>
  В степи под Херсоном —<br>
  Высокие травы,<br>
  В степи под Херсоном — курган.<br>
  Лежит под курганом,<br>
  Заросшим бурьяном,<br>
  Матрос Железняк, партизан.<br>
  <br>
  Он шел на Одессу,<br>
  А вышел к Херсону —<br>
  В засаду попался отряд.<br>
  Налево — застава,<br>
  Махновцы — направо,<br>
  И десять осталось гранат. <br>
  <br>
  «Ребята, — сказал,<br>
  Обращаясь к отряду,<br>
  Матрос-партизан Железняк, —<br>
  Херсон перед нами,<br>
  Пробьемся штыками,<br>
  И десять гранат — не пустяк!»<br>
  <br>
  Сказали ребята:<br>
  «Пробьемся штыками,<br>
  И десять гранат — не пустяк!»<br>
  Штыком и гранатой<br>
  Пробились ребята...<br>
  Остался в степи Железняк. <br>
  <br>
  Веселые песни<br>
  Поет Украина,<br>
  Счастливая юность цветет.<br>
  Подсолнух высокий,<br>
  И в небе далекий<br>
  Над степью кружит самолет.<br>
  <br>
  В степи под Херсоном —<br>
  Высокие травы.<br>
  В степи под Херсоном — курган.<br>
  Лежит под курганом,<br>
  Заросшим бурьяном,<br>
  Матрос Железняк, партизан.<br>
  <br>
  1936<br>
  <br>
        <em>Русские советские песни (1917-1977) / Сост. Н. Крюков и Я. Шведов. 
        М.: Худож. лит., 1977.</em><br>
 <br>
 <br>
  Наиболее известная мелодия песни принадлежит Матвею Блантеру. Кроме того, есть мелодия Григория Сметанина. В годы Великой Отечественной войны песня была популярна у партизан.<br>
<br>
        В авторском стихотворении Михаила Голодного противники Железняка не названы, а сам герой - условный. Однако в народе он стал ассоциироваться с участником революции 1917 г. и Гражданской войны балтийским матросом-анархистом Анатолием Железняковым (1885-1919). Реальный Железняков на Одессу не шел и погиб не под Херсоном. <br>
        <br>
        Александр Железняков прославился в январе 
        1918 г. разгоном Учредительного собрания. «Караул 
        устал!» - заявил он засидевшимся допоздна депутатам. Затем занимал командные должности в Красной Армии, несколько 
        раз объявлялся вне закона большевистской властью. Погиб в красном партизанском 
        отряде в 1919 г. под Екатеринославом. <br>
  <br>
        Поэт Михаил Голодный был очевидцем Гражданской войны на 
        Украине. До 1920 г. жил в Екатеринославе (ныне Днепропетровск), где 
        в 1919 г. вступил в комсомол, затем переехал в Харьков - столицу Советской 
        Украины. В начале 1920-х гг. вместе с екатеринославскими комсомольскими 
        приятелями - начинающими поэтами Михаилом Светловым (будущим автором <a href="grenada.php">&quot;Гренады&quot;</a>) 
        и Александром Ясным, - уехал учиться литературе в Москву. Автор стихов популярных 
        песен о Гражданской войне: <a href="ptchapajevtsa.php">&quot;Песня чапаевца&quot;</a>, 
        <a href="poschorse.php">&quot;Песня о Щорсе&quot;</a> и &quot;Партизан 
        Железняк&quot;.<br>
  <br>
  <br>
  <img src="../../kukly/1917/zeleznak.jpg" width="286" height="394"><br>
        <em>Матрос Железняк (&quot;Караул устал&quot;), кукла, ок. 2001</em><br>
  <br>
  <br>
        У песен &quot;Партизан Железняк&quot;
        (музыка Матвея Блантера, слова Михаила Голодного, 1935), <a href="pokahovke.php">"Песня о Каховке"</a> (музыка Исаака Дунаевского, слова Михаила Светлова, 1936) и <a href="orlenok.php">&quot;Орленок&quot;</a> 
        (музыка Виктора Белого, слова Якова Шведова, 1936) одинаковая мелодия и манера исполнения, 
        несмотря на разных композиторов. Это еврейская фольклорная мелодия. Возможно, 
        композиторы заимствовали ее неосознанно.<br>
  <br>
        В августе 2005 г. от анархистов из Иваново был записан фрагмент переделки 
        этой песни про то, как отряду Железняка в украинской степи не хватило 
        травы и выпивки. Закончилась песенка так:<br>
