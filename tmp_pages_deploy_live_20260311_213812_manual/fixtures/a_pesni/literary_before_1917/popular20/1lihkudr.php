<br>
<br>
Слова Алексея Кольцова<br>
<br>С радости, веселья<br>
  Хмелем кудри вьются;<br>
  Ни с какой заботы<br>
  Они не секутся.<br>
<br>Их не гребень чешет, -<br>
  Золотая доля,<br>
  Завивает в кольцы<br>
  Молодецка удаль.<br>
<br>Не родись богатым,<br>
  А родись кудрявым;<br>
  По щучью веленью<br>
  Все тебе готово.<br>
<br>Чего душа хочет,<br>
  Из земли родится;<br>
  Со всех сторон прибыль<br>
  Ползет и валится.<br>
<br>Что шутя задумал,<br>
  Пошла шутка в дело;<br>
  А тряхнул кудрями –<br>
  В один миг поспело.<br>
<br>Не возьмут где лоском,<br>
  Возьмут кудри силой;<br>
  И что худо – смотришь,<br>
  Пол воде поплыло;<br>
<br>Любо жить на свете<br>
  Молодцу с кудрями,<br>
  Весело на белом<br>
  С черными бровями.<br>
<br>Во-время да в пору<br>
  Медом речи льются;<br>
  И с утра до ночи<br>
  Песенки поются.<br>
<br>Про те речи-песни<br>
  Девушки все знают<br>
  И о кудрях зиму,<br>
  Ночь не спят, гадают.<br>
<br>Честь и слава кудрям!<br>
  Пусть их волос вьется;<br>
  С ними все на свете<br>
  Ловко удается.<br>
<br>Не под шапку горе<br>
  Голове кудрявой!<br>
  Разливайтесь, песни!<br>
  Ходи, парень, браво!<br>
<br>1837<br>
<br>
