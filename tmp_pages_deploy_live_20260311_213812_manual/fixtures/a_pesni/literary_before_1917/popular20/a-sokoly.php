  <strong>Аннотация.</strong> Предметом исследования является популярная солдатская строевая песня  "Взвейтесь, соколы, орлами". Целью работы является: проследить историю  песни с момента её первого появления, первого публичного исполнения,  первой публикации. Результат работы - актуализация фактов первого  исполнения её в 30-х годах XIX столетия, именно в 1834 году, во время  Калишских маневров, а не в период Отечественной войны 1812 г. Результаты  работы могут использоваться в подготовке к публикации песенных  сборников. Область применения результатов - солдатская песня, военная  история, сохранение культурного наследия, песенное искусство, история  русского народа в контексте изучения песенного искусства. Метод  исследования - поиск и изучение всех упоминаний песни в печатных  источниках, аудио-визуальных исполнениях. Изучение существующих песенных  солдатских сборников на русском и иностранных языках. Соблюдается  хронологический порядок и опора на исторически достоверные факты.   Выводы: Старинная солдатская строевая песня "Взвейтесь, соколы, орлами"  является наиболее любимой и популярной среди военнослужащих. Песня имеет  богатую историю, связанную с героизмом русского народа во время войн.  Различные варианты песни с уверенностью позволяют указать на широту  творческих поисков солдатских сочинителей.Песня актуальна и для  современных творческих работников - явилась поводом для создания  телефильма.<br>
  <br>
  <strong>Ключевые слова:</strong> солдатская песня,  союз с Пруссией, Калишские маневры, Великая Отечественная война, военная  история, культурное наследие, Лагерь, хоровое пение, русский царь,  прусский король<br>
