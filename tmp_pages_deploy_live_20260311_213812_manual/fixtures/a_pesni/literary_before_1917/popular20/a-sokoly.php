    <br>
    В наиболее раннем «Сборнике солдатских, казацких и  матросских песен» Н.Х. Весселя (слова) и В.К. Альбрехта (музыка),  вышедшем в С-Петербурге в 1875 году, песня имеется в том же варианте, но  последний куплет звучит так:<br>
    <br>Все покорны царской воле,<br>
    По отбою кончен спор.<br>
    Во потешном ратном поле<br>
    Поёт песню дружно хор:<br>
    Взвейтесь, соколы, орлами,<br>
    Полно горе горевать,<br>
    То ли дело под шатрами<br>
    В поле лагерем стоять! [6]<br>
    <br>Мелодическая линия в сборнике Весселя и Альбрехта  отличается довольно сильно от современного варианта Мантулина. Песня  под номером 12 (см. рис. ниже) поётся на мотив кавказской песни «Славно,  весело, ребята» № 10, также как и номер 11. Вероятно, слово  «кавказская» в заголовке песни 12 и послужило основанием для отнесения  сегодняшних «Соколов» также к кавказским песням.<br>
    <br> <img src="a-sokoly2.jpg"><br>
    <br><img src="a-sokoly3.jpg"><br>
    <br>
    Несколько изменённый мотив, но на те же слова,  имеется и в песенном «Сборнике избранных песен для солдатского хорового  пения» П.Ф. Егорова, вышедшего в Екатеринославе в 1893 году [7].<br>
    <br> <img src="a-sokoly4.jpg"><br>
    <br>Как мы можем видеть, мотив довольно сильно отличается от привычного нам сегодня «мантулинского» варианта.<br>
    <br>
    Однако, максимально близкая к современной мелодия  этой песни находится в Сборнике 123 военно-исторических песен «Боевые  песни русского солдата» капельмейстера 122-го пехотного Тамбовского  полка поручика Г.М. Попова, вышедшего в 1893 году в издательстве В.  Березовского (С-Петербург, ул. Колокольная, 14) и в его же «Сборнике  боевых, бытовых и плясовых солдатских песен в 2-х частях», вышедшего в  Харькове в 1888 году. Это песня № 70, «Освежение союза с Пруссией (в  1835 г.)» и песня № 6 «Русский царь собрал дружину». Слова этой песни  следующие [8, 9]:<br>
    <br>Русский Царь собрал дружину,<br>И велел своим орлам:<br>Плыть по морю на чужбину<br>В гости к добрым Пруссакам.<br>
    <br>
    <em>Припев:</em><br>Нас лелеял Царь державный,<br>Слава Белому Царю!<br>Слава Руси православной<br>И родному Королю!<br>
    <br>Не на бой спешим мы драться,<br>Не крамольников смирять,<br>Но с друзьями повидаться,<br>Пруссаков спешим обнять!<br>
    <br>
    <em>Припев.</em><br>
    <br>Кровь Царей нас породнила,<br>Славный бой нас подружил,<br>Сам творец, во бранях сильный,<br>Наш союз благословил.<br>
    <br>
    <em>Припев.</em><br>
    <br>Пусть завистники восстанут,<br>Разорвут союз друзей –<br>Там следа врагов не станет,<br>Где пройдут полки Царей!<br>
    <br>
    <em>Припев.</em><br>
    <br> <img src="a-sokoly5.jpg"><br>
    <br>Следует заметить: темп «Медленно» говорит о том,  что песня изначально не предназначалась как строевая. Это гимн. Слова и  музыку этого варианта, как указано в сборнике 1893 г., написал музыкант  лейб-гвардии Егерского полка Григорий Малышев, бывший в лагере под  Калишем. «Государю Николаю Павловичу эта песня так понравилась, что он  подарил сочинителю золотые часы, песня была размножена и роздана в роты  Калишского отряда, участвующего в знаменитых манёврах и разучена всеми  песельниками. Также для совместного исполнения русскими и пруссаками  песня была переведена и на немецкий язык».<br>
    <br>
    Немного предыстории. Пруссаки готовы были  восстать и отдать всё, чтобы освободиться от ига Наполеона. Поэтому для  войны 1813-1814 гг. между королём Пруссии Фридрихом-Вильгельмом III и  нашим императором Александром I был заключён 16 февраля 1813 года в г.  Калише союз, по которому было решено не заключать отдельно ни мира, ни  перемирия с Наполеоном. В 1835 г. император Николай I пожелал освежить  этот союз и скрепить его дружественными отношениями между русскими и  прусскими войсками, для чего, по общему соглашению, решено было собрать у  Калиша с 15 августа по 15 сентября части обоих армий для совокупных  манёвров в общем лагере [8, С. 54]. Вот как описывает это знаменательное событие «Журнал пребывания Е. И. В. в г. Калише в августе-сентябре 1835 г.».<br>
    <br>«В 6 часов пополудни (30 августа 1835 г., первый  день приезда Прусского короля Фридриха Вильгельма) на площади у дворца  были собраны для общей вечерней зари музыканты, горнисты, барабанщики и  флейщики всех пехотных войск, в сборе при Калише находящихся, в числе  музыкантов 723, горнистов 586, барабанщиков 825, флейщиков 139, рядовых  состоящих в музыкантских хорах 118, всего всех 2391.<br>
    <br>Музыканты были выстроены в одну линию по  старшинству дивизий, справа налево фронтом к королевскому балкону, на  боковых фасах площади между музыкантами и дворцом находились влево от  дворца все гг генералы, штаб- и обер-офицеры сводного гвардейского  отряда и кавалерийские, вправо все гг генералы, штаб- и обер-офицеры  пехоты 3-го пехотного корпуса, по старшинству дивизий, бригад и полков; и  сверх того, находились у Зори все гг генералы, штаб- и обер-офицеры как  российские, не вошедшие в состав вышеупомянутых войск, так и  иностранные.<br>
    <br>В 6 часов десятью музыкантами пробита была  Повестка. После того музыканты всех полков играли общим хором разные  марши и в том числе сочинённый его величеством королём Прусским и  сочинённый ея высочеством супругою принца Вильгельма Прусского.<br>
    <br>
    Вскоре после того, как музыка начала играть, его  величество король вышел на балкон; при выходе его все офицеры  приветствовали его величество восклицанием «Ура!» и вслед за тем музыка,  перестав играть марши, заиграла гимн «Боже, царя храни!» В 7 часов  вечера пробита была на месте общая Зоря, а всеми исполнена «Русский царь  собрал дружину», и потом музыканты играли общим хором молитву» [10].<br>
    <br>Упоминаемый Поповым вариант песни на немецком  языке опубликован в сборнике «Die beliebtesten Russischen national und  volkslieder» (Лучшие русские народные песни), вышедшем в издательстве  Шлезингера (Берлин) примерно в 50-х годах XIX века и хранящийся ныне,  также как и все остальные песенники, в Российской государственной  библиотеке (Отдел нот и звукозаписей, Пашков-Дом). Вот этот текст:<br>
    <br>Lusst uns tren und einig sein, und stimmt in frohe Lieder ein<br>Stosst die rollen Glaeser an, und niedre Furecht rerlaesst uns dann,<br>es lebe,  uns eng verband, der Kaiser und das Vaterland!<br>Mu thig lusst zum Kumpf uns gehu, und fest dem Feind in’s Auge sehn,<br>Blut und Leben bringet dur dem thenren Land, das uns gebar, ja<br>
    selbst ron Feinden rings bedroht, sei eure Losung: Sieg oder Tod! [18]<br>
    <br>Имеющиеся в нотах мелодические отклонения  выражены в виде традиционного для русских солдатских песен нисходящего  хода по звукам арпеджио в первой фразе. Вероятно, это очередное  мелодическое многообразие, но теперь применённое немцами.<br>
    <br>
    В «Сборнике русских, казачьих, военных и  исторических песен для Донского кадетского корпуса» 1880-х годов песня  «Взвейтесь, соколы, орлами» приведена на другой, отличный от всех  перечисленных, мотив [11].<br>
    <br> <img src="a-sokoly6.jpg"><br>
    <br> Есть в этом сборнике великолепная  аранжировка для кадетского хора.  Это также песня «Русский царь» с  современным мотивом «Взвейтесь, соколы орлами». Темп «Торжественно»  также говорит о гимническом характере песни (см. пример ниже):<br>
    <br> <img src="a-sokoly7.jpg"><br>
    <br>В сборнике из 130 избранных хоровых военных песен  «Военный досуг», выпущенном в 1912 году издательством Циммермана в  Петербурге, «Русский царь» со своим мотивом отсутствует, но есть песня №  90 «Взвейтесь, соколы, орлами» на совершенно иной, отличный от всех,  мотив.<br>
