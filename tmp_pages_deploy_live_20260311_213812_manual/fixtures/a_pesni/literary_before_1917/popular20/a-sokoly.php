    <br>К сожалению, точно не известно, Александрову ли  принадлежит идея соединить мотив песни «Освежение союза с Пруссией  (Русский царь собрал дружину)» со словами Шилова и песней «Взвейтесь,  соколы, орлами», либо кто-то другой, но в результате сегодня мы имеем  великолепную и популярнейшую солдатскую строевую песню, к которой и  бойцы и командиры относятся с большим благоговением, потому что она  пришла к нам из глубины времён и в полной мере отражает характер и  настроение русского солдата.<br>
    <br>Итак, в начале XX века песня приобрела  современный формат, однако, многочисленные варианты живут сегодня  самостоятельной жизнью. Песня является памятником не только культурного  наследия России, но и мирового культурного наследия.<br>
    <br>Песня солдатская сегодня выполняет наряду с  обычной своей функцией, и «боевую» задачу – воспитательную и  вдохновляющую. Трудно сказать, какова была бы сегодня история России, не  будь у её народа такого богатства – солдатских строевых песен, но  своими песнями наша страна гордится и, по признанию многих зарубежных  исследователей, они являются лучшими. А способность русского народа  переделывать и изменять слова и мелодии песен говорит о необыкновенной  его музыкальной даровитости.<br>
    <br>Русская мужская воинская песня, особая манера  исполнения – все это до сих пор живо, пока есть люди, которые сохраняют  традицию. К сожалению, в советское время носители и исполнители  «старорежимной» воинской песни были практически уничтожены (ликвидация  казачества, старого офицерства, дореволюционных воинских традиций).  Многое делалось для того, чтобы, говоря о русской народной песне, люди  представляли себе тоскливое или нарочито веселое пение хора бабушек, а  при упоминании военной песни – сладкоголосое пение солистов военных  ансамблей. С такими песнями в бой не пойдешь! Именно поэтому необходимо  срочно возвращать и улучшать традиционную песенную культуру, прежде  всего в войсках. Песни в народе и в армии продолжают жить, видоизменяясь  и возвращаясь «на круги своя».<br><br><br>
