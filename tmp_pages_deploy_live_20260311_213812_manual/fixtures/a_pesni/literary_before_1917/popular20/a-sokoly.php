	<br>Солдат без песни – что без ружья, говаривал  великий русский полководец генералиссимус Александр Васильевич Суворов.  Знаток солдатской души, гениальный стратег понимал, что солдатская песня  – это огромный пласт русской воинской культуры, и без нее немыслимо  традиционное воспитание воина и жизнь и быт солдата на службе.<br>
    <br>
    Дело в  том, что солдатские песни во многом  историчны, так как связаны с теми или иными батальными событиями. И в  этом их воспитательное значение. При изучении солдатских песен нужно  иметь в виду, что в них проникли державные взгляды «сверху» [3, 4]. Например, Великий князь Константин Романов известен как автор популярной строевой песни «Наш полк»:<br>
    <br>Наш полк. Заветное чарующее слово<br>Для тех, кто смолоду и всей душой в строю.<br>Другим оно старо, для нас - все так же ново<br>И знаменует нам и братство, и семью.<br>О, знамя ветхое, краса полка родного,<br>Ты, бранной славою венчанное в бою.<br>Чье сердце за твои лоскутья не готово<br>Все блага позабыть и жизнь отдать свою.<br>Полк учит нас терпеть безропотно лишенья<br>И жертвовать собой в пылу святого рвенья.<br>Все благородное: отвага, доблесть, долг,<br>Лихая удаль, честь, любовь к отчизне славной<br>К великому Царю и вере православной<br>В едином слове том сливается: наш полк.<br>
    <br>    <em>К.Р.  (Константин Романов)<br>
    Красное Село, 31 мая 1899 г.</em><em></em><br>
 <br>
    <br>
    Но всё же более глубокие корни – народные. В  данной статье речь идёт об одной из старинных солдатских строевых песен  «Взвейтесь, соколы, орлами», много лет пользующейся огромной  популярностью среди военнослужащих. Она является интересной для  исследователей, поскольку используется в армии достаточно давно и  имеется множество её вариантов, каждый из которых уже оброс своей  отдельной историей. Песню относят ко временам Кавказских войн 1817-1864  гг., однако некоторые исследователи говорят, что она из эпохи  Отечественной войны 1812 года. Безусловно, песня лагерная, то есть,  исполняемая в полевых лагерях, о чём говорят и её слова, и первое её  появление и применение в Калишском лагере в 1835 году. К разочарованию  многих, установлено, что песня всё-таки сочинена и впервые прозвучала на  Калишских маневрах 1935 года, но об этом ниже. Современный вариант (по  В. Мантулину, 2004 г.), исполняемый сегодня в войсках, следующий:<br>
    <br>Взвейтесь, соколы, орлами,<br>
    Полно горе горевать!<br>
    То ли дело под шатрами<br>
    В поле лагерем стоять.<br>
    Там бел город полотняный,<br>
    Морем улицы шумят,<br>
    Позолотою румяной<br>
    Медны маковки горят.<br>
    Там, едва заря настанет, -<br>
    Строй пехотный закипит,<br>
    Барабаном в небо грянет,<br>
    И штыками заблестит.<br>
    Закипит тогда войною<br>
    Богатырская игра,<br>
    Строй на строй пойдёт стеною,<br>
    И прокатится «Ура»! [5]<br>
