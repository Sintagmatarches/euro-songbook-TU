  <span class='bold'>История гимна</span><br>
  <br>
  Стихотворение Василия Жуковского &quot;Боже, царя храни...&quot; - вольный перевод 
  начальных строк английского гимна "God Save the King" (или "God Save the Queen" 
  - если на троне была королева) - было написано в 1813 (по другим данным - в 
  1814) г., адресовано Александру I и озаглавлено &quot;Молитва Русского народа&quot;:<br>
  <br>
  Боже, Царя храни!<br>
  Славному долги дни <br>
	<span class='refren'>Дай на земли!<br></span>
  Гордых смирителю, <br>
  Слабых хранителю, <br>
  Всех утешителю <br>
	<span class='refren'>Все ниспошли!<br></span>
  <br>
  Впервые в качестве русского национального гимна прозвучал в 1816 г. на параде 
  в Варшаве, устроенном по поводу встречи Александра I. Исполнялся на мелодию 
  английского гимна и обязательным для исполнения не был - существовал на правах 
  неофициального гимна.<br>
  <br>
  Осенью 1816 г. для торжества по поводу пятилетия Царскосельского Лицея возникла 
  идея исполнить полный русский текст, и для этого еще две строфы к тексту Жуковского 
  дописал лицеист Пушкин - вероятно, по просьбе лицейского руководства: <br>
  <br>
  Там - громкой славою<br>
  Сильной державою <br>
	<span class='refren'>Мир он покрыл. <br></span>
  Здесь безмятежною <br>
  Сенью надежною <br>
	<span class='refren'>Нас осенил. <br></span>
  <br>
  Брани в ужасный час<br>
  Мощно хранила нас<br>
	<span class='refren'>Верная длань -<br></span>
  Глас умиления,<br>
  Благодарения<br>
  Сердца стремления -<br>
	<span class='refren'>Вот наша дань.<br></span>
  <br>
  С новыми строфами гимн был исполнен хором на торжестве 14 октября 1816 г. 
  - день рождения вдовствующей императрицы Марии Федоровны и день основания Лицея. 
  За пределами Лицея пушкинские строфы в гимн никогда не входили, а распространенное 
  мнение о том, что Пушкин является автором двух строф окончательного текста, 
  ошибочно. Эти строфы вообще нельзя петь на современную мелодию гимна.<br>
  <br>
  В 1818 г. Жуковский сам дописал еще пять строф к своему стихотворению (всего строф 
  получилось шесть) и опубликовал новую версию в журнале &quot;Сын Отечества&quot; 
  под заглавием &quot;Гимн, петый воспитанниками санкт-Петербургской гимназии 
  на публичном экзамене&quot;: <br>
  <br>
  Боже, Царя храни!<br>
  Славному долги дни<br>
  <span class='refren'>Дай на земли;<br></span>
  Гордых смирителю,<br>
  Слабых хранителю,<br>
  Всех утешителю<br>
  <span class='refren'>Все ниспошли!  <br></span>
