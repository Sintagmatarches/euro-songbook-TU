Перводержавную,<br>
    Русь православную,<br>
	<span class='refren'>Боже, храни!<br></span>
    Царство ей стройное!<br>
    В силе спокойное!<br>
    Все ж недостойное<br>
    <span class='refren'>Прочь отжени!  <br></span>
