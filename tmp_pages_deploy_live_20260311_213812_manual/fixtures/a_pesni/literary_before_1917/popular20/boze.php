  <span class='bold'>История английского предшественника</span><br>
  <br>
  "God Save the King" (текст и ноты) написал в 1740 г. Генри Кэри, побочный 
  сын лорда Галифакса. В 1743 г. он покончил жизнь самоубийством, оставив в нищете 
  жену с шестью детьми, а его имущество пошло с молотка. Через три десятка лет, 
  в 1774 г., гимн был впервые напечатан без указания авторства. На следующий 
  год вспыхнуло восстание в Шотландии, и тогда противники шотландской независимости 
  взяли этот гимн на вооружение как &quot;Гимн лояльности&quot; английскому королю. 
  Вскоре он получил общеевропейскую популярность, был переведен на разные языки 
  и стал использоваться в качестве национального гимна в разных странах Европы. 
  На русский язык впервые переведен немцем Остеном (впоследствии - известным русским 
  лингвистом, принявшим имя Александр Христофорович Востоков). <br>
  <br>
  <br>
  <span class='bold'>Пародии на &quot;Боже, Царя храни!&quot;</span><br>
  <br>
  Гимн «Боже, царя храни!» (и до введения в 1833 г. новой мелодии, и после нее) 
  пародировался на протяжении всей дальнейшей истории Российской империи. 
  Один из из первых пародистов - художник Уткин - за свое творение был пожизненно 
  заключен в Шлиссельбург, где и умер; такие же приговоры получили еще двое проходивших 
  по делу - поэт Соколовский и офицер Ибаев, а шестеро московских студентов, включая 
  Герцена и Огарева, были сосланы в отдаленные части Империи.<br>
