    Правды блюстителей,<br>
	<span class='refren'>Боже, храни!<br></span>
    Жизнь их примерную,<br>
    Нелицемерную,<br>
    Доблестям верную<br>
    <span class='refren'>Воспомяни!  <br></span>
  <br>
О, Провидение!<br>
    Благословение<br>
    <span class='refren'>Нам ниспошли!<br></span>
