    <span class='refren'>Сердцу сияй!<br></span>
  <br>
  В 1825 г. на трон вступил Николай I. В 1833 г. он выразил желание иметь собственную 
  мелодию гимна, отличную от английской, и через шефа Жандармского корпуса графа 
  Бенкендорфа поручил ее создание Алексею Львову. Львов возглавлял канцелярию 
  III Отделения, а также имел музыкальные способности (в 1837 возглавил Придворную 
  певческую капеллу). Он и написал новую мелодию. При этом изменился музыкальный 
  размер, и текст Жуковского, подходивший для английской мелодии, на мелодию Львова 
  не ложился. Тогда Жуковский отредактировал текст, и при этом появилась строчка 
  <em>&quot;Сильный, Державный!&quot;</em>, которая, вероятно, действительно навеяна 
  пушкинской лицейской строчкой <em>&quot;Сильной державою&quot; </em>(Жуковский, 
  разумеется, знал о тексте Пушкина).<br>
  <br>
 Первые исполнения нового гимна состоялись 23 ноября 1833 г. в Петербурге 
  в зале Придворной певческой капеллы в присутствии императора Николая 
  I и его семейства, 11 декабря того же года в Москве в Большом 
  театре (исполнен стихийно труппой театра после представления) и, наконец, официальное 
  исполнение 25 декабря, в годовщину изгнания наполеоновских 
  войск, снова в Петербурге в Большой Анфиладе Зимнего дворца. 
  С 31 декабря 1833 г. стал официальным национальным гимном, обязательным (принудительным) 
  для исполнения. (Из записки великого князя Михаила Павловича за номером 2344: <em>&quot;[31 декабря 1833 года] Государю Императору благоугодно было изъявить 
  свое соизволение, чтобы на парадах, смотрах, разводах и в прочих случаях вместо 
  употребляемого ныне гимна, взятого с национального английского (Боже, царя храни), 
  играли музыканты вновь сочиненную на сей же гимн музыку"</em>). <br>
  <br>
  Радикальные круги встретили новую версию отрицательно. Во-первых, из-за принудительности 
  и, во-вторых, из-за сомнительного происхождения - музыка написана жандармом. 
  <br>
  <br>
  Подробнее об истории и биографии Львова см. <a href="a-lvov.htm">Наталия Рамазанова. 
  А.С. Пушкин и А.Ф. Львов. К истории создания "Народного гимна"</a> (Нева. 2001. №2. С. 214-219). <br>
