        <span class="text"> <span class="header"> <strong>ЕДУТ, ПОЮТ ЮНКЕРА ГВАРДЕЙСКОЙ 
        ШКОЛЫ</strong></span> <br>
        <br>
        1. Едут, поют юнкера Гвардейской школы,<br>
        Трубы, литавры на солнце блестят. <br>
        <br>
        <em>Припев (2 раза):</em> <br>
        <br>
        Грянем «Ура!», лихие юнкера,<br>
        За матушку Россию, за русского Царя! <br>
        <br>
        2. Справа повзводно, сидеть молодцами,<br>
        Не горячить понапрасну коней. <br>
        <br>
        3. Наш эскадронный скомандовал нам «Смирно»,<br>
        Руку свою приложил к козырьку. <br>
        <br>
        4. Справа и слева идут гимназисточки.<br>
        Как же нам, братцы, равненье держать? <br>
        <br>
        5. Съемки примерные, съемки глазомерные,<br>
        Вы научили нас женщин любить. <br>
        <br>
        6. Здравствуйте, барышни, здравствуйте, милые,<br>
        Съемки у нас, юнкеров, начались! <br>
        <br>
        <em>Еще один куплет, порядковый номер которого не известен: </em><br>
        <br>
        Тронулся, тронулся, заколыхался<br>
        Алою лентою наш эскадрон. <br>
        <em><br>
        Георгий Вилинбахов. Комментарий к «Запискам кирасира» Владимира Трубецкого 
        (В. С. Трубецкой. Записки кирасира: Мемуары. М., Россия, 1991. С. 214-215). 
        </em><br>
        <br>
        <br>
        Песня Николаевского кавалерийского училища о выезде на топографические 
        учебные съемки. Они проводились в Дудергофском лагере под Красным Селом. 
        Юнкера там пользовались относительной свободой. Ночевали в барачных казармах, 
        а днем были в поле поодиночке или группами, при этом имели возможность 
        общаться с местным населением, покупать выпивку и закуску у бродячих торговцев 
        провиантом - «шакалов» (см. о них песню <a href="podorozke.php">«По дорожке 
        Красносельской»</a>). Бывало, вспыхивали любовные романы. Действовали 
        в районе лагеря и бордели.<br>
        <br>
        Георгий Вилинбахов приводит и другой вариант припева («Буль-буль-буль 
        бутылочка, любимая моя» или «Буль-буль-буль бутылочка казенная моя»), 
        с пояснением: «Считается, что этот припев появился во время первой мировой 
        войны в Елизаветградском и Терском кавалерийских училищах. Его пели во 
        время Гражданской войны. (Первая строчка припева была - «Гей, песнь моя 
        любимая...»)». <br>
        <br>
        Но такая датировка припева с бутылочкой противоречит самим мемуарам Владимира 
        Трубецкого, который упоминает «Буль-буль-буль бутылочка, любимая моя» 
        в числе песен, исполнявшихся еще до войны в Лейб-гвардии Кирасирском Ее 
        Величества полку («Синие кирасиры»), где он служил с 1911 года. Полк квартировался 
        в Гатчине.<br>
        <br>
        Вот фрагмент описания летнего полкового учения 1912 года: <br>
        <br>
        «Полковое учение заканчивалось двумя «безумными» кавалерийскими атаками 
        в сомкнутом и разомкнутом строю под раскатистое «ура» всего полка, когда 
        по команде «марш-марш!!» - мы ураганом летели с пиками наперевес и обнаженными 
        шашками на так называемого «обозначенного противника», состоявшего из 
        нескольких конных кирасир, державших в руках различные пестрые флажки. 
        Флажки эти условно обозначали то или иное воинское соединение мнимого 
        неприятеля. После атаки генерал обычно громогласно благодарил полк за 
        «лихое ученье». Полк снова выстраивался в походный порядок и величавым 
        шагом возвращался домой. Раздавалась команда: «Песельники, вперед!» - 
        и в голову каждого эскадрона выезжали лихие песельники со своими голосистыми 
        запевалами. Они услаждали нас веселыми или заунывными солдатскими песнями, 
        в которых либо восхвалялась какая-то красавица Дуня, либо доблесть российского 
        оружия, либо, наконец, - «Буль-буль-буль-бутылочка, любимая моя!» Песен 
        этих было бесконечное множество, и некоторые из них были очень старинными. 
        В песельники большей частью добровольно шли хохлы — большие любители всякой 
        музыки. Хотя люди и пели по приказанию, но приказание это выполняли всегда 
        с большой охотой».<br>
        <br>
        <em>В. С. Трубецкой. Записки кирасира: Мемуары. М., Россия, 1991. С. 110-111.</em><br>
        <br>
        <br>
        Георгий Вилинбахов в комментариях (там же, с. 214) пишет, что в советское 
        время на мелодию этой песни пели пионерскую песню, начинавшуюся словами: 
        <br>
        <br>
        Смотрит вожатый, смотрят пионеры,<br>
        Что за отряд показался вдали. <br>
