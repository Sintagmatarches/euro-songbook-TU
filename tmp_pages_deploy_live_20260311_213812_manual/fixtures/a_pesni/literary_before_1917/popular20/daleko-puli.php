	<span class="header"><strong>ДАЛЕКО 
  В СТРАНЕ ИРКУТСКОЙ</strong></span><br>
  <br>
Далеко в стране Иркутской<br>
Там построен большой дом,<br>
Обнесен кругом оградой,<br>
Арестанты сидят в нем.<br>
В одной камере тюремной<br>
Друг к расстрелу присужден.<br>
Рано утром на рассвете<br>
Ключник в камеру зашел,<br>
«Собирайся, друг, с постели,<br>
На расстрел тебя – пора».<br>
И друг скоренько собрался,<br>
Разом вышел со двора.<br>
У тюрьмы народ скопился,<br>
С темной ноченьки глухой.<br>
«Ты послушай, люд крещенный,<br>
Ты, свидетель будешь мой.<br>
Я не преступник уголовный,<br>
Я не вор и не злодей.<br>
В Александровский попался,<br>
Нарушив закон царей.<br>
<br>
<em>Записана от инвалида первой мировой войны Анания Разуваева, 
  с. Б. Уро Баргузинского аймака, БМАССР, авг. 1936.<br>
  <br>
Элиасов Л. Е. Народная революционная поэзия Восточной Сибири эпохи гражданской 
  войны. Улан-Удэ, 1957, стр. 117-118.<br></em><br>
  <br>
  На основе песни &quot;Далеко в стране Иркутской&quot; (она же <a href="dalekovstrane.php">&quot;Александровский централ&quot;</a>) о быте пересыльной тюрьмы, текст которой был создан не позднее 1880-х годов. Предположительно, это вариант начала революции 1905 года. В песне 
  появилось упоминание РАССТРЕЛА. Именно с этого времени в России в массовом порядке 
  стали применяться расстрелы как форма казни. Ранее, в XIX веке, смертная казнь 
  вообще была редкостью (официально была отменена с XVIII века и применялась лишь 
  в исключительных случаях - декабристы, народовольцы) и обычно через повешение. 
  Традиция расстрелов в России не прерывалась вплоть до конца XX века.<br>
  <br>
  Также см. песню <a href="siditvkamere.php">&quot;Сидит в камере мальчишка&quot;</a> с тем же сюжетом - расстрелом заключенного, в которого попадает лишь одна пуля из многих.<br>
