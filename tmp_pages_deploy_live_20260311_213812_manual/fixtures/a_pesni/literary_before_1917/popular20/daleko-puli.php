1. Далеко в стране Иркутской...</strong><br>
  <br>Далеко в стране Иркутской<br>
	  Там построен большой дом,<br>
	  Обнесен большой оградой,<br>
	  Арестанты сидят в нем.<br>
	  Там сидит прекрасный юныш,<br>
	  Был к расстрелу пригворен.<br>
	  Рано утром на рассвете<br>
	  Ключники в камеру зашел.<br>
	  - Собирайся, друг, с постели,<br>
	  Смертный час тебе пришел.<br>
	  Юныш весело собрался<br>
	  Разом вышел со двора.<br>
	  Народ собрался,<br>
	  Юныш плакал, весь в слезах,<br>
	  - Ты не плачь, народ крещщеный,<br>
	  Я свидетель буду твой.<br>
	  - Я преступник уголовной<br>
	  Был к расстрелу приключен.<br>
	  Девятнадцать пуль злодеек<br>
	  Все взвилися в облака,<br>
	  А двадцатая злодейка<br>
	  Прямо к сердцу прилегла. <br>
  <br>
  <em>Текст записан т. Дмитриевым К.А. со слов т.т. Греблищикова А.Д., Лобазерова Г.Т. И Солодухина в с. Б. Куналей, Тарбагатайского района, БМАССР, 1936 г. <br>
  <br>Эта песня относится к циклу популярной песни об Александровском централе. О ней см. подробную библиографию в сб. А.В. Гуревича «Фольклор Восточной Сибири», ОГИЗ, Иркутск, 1938 г., стр. 157. <br>
  <br>Гуревич А.В., Элиасов Л.Е. Старый фольклор Прибайкалья. Том первый. Улан-Удэ, 1939. С. 4-5. Раздел «Бродяжьи тюремные песни», №7. </em><br>
