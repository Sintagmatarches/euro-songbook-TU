  <strong>2. Александровский централ</strong><br>
  <br>
  Далеко в стране Иркутской, <br>
  Между двух высоких скал, <br>
  Окружен большим забором <br>
  Александровский централ. <br>
  <br>
  Солнце всходит и заходит, <br>
  А в тюрьме моей темно. <br>
  Часовые днем и ночью <br>
  Стерегут мое окно. <br>
  <br>
  Ничего в тюрьме не видно, <br>
  Ничего там не слыхать, <br>
  Только слышно, кандалами <br>
  Заключенные гремят. <br>
  <br>
  Ключник двери отворяет: <br>
  - Выходи-ка, брат, сюда. <br>
  Посреди двора священник <br>
  Стал споведовать меня. <br>
  <br>
  - Ты скажи, скажи, разбойник, <br>
  Сколько душ ты загубил? <br>
  - Двадцать пять я православных, <br>
  Некрещеных - без числа. <br>
  <br>
  - Некрещеных мы прощаем, <br>
  Православных - никогда! <br>
  Присудил мне тот священник <br>
  Сто пять выстрелов в меня. <br>
  <br>
  Девятнадцать пролетело <br>
  Мимо буйной головы, <br>
  А двадцатая, злодейка, <br>
  Загубила жизнь мою. <br>
  <br>
  В воскресенье мать-старушка <br>
  К воротам тюрьмы пришла, <br>
  Своему родному сыну <br>
  Передачу принесла. <br>
  <br>
  Говорит тут ей охрана: <br>
  - Передачу забери, <br>
  Нет в живых уж тваво сына, <br>
  Расстреляли мы его. <br>
  <br>
  Побледнела мать-старушка, <br>
  От ворот тюрьмы пошла. <br>
  И никто о том не знает, <br>
  Что в душе она несла. <br>
  <br>
  <em>А я не уберу чемоданчик! Песни студенческие, школьные, дворовые 
  / Сост. Марина Баранова. - М.: Эксмо, 2006.</em><br>
