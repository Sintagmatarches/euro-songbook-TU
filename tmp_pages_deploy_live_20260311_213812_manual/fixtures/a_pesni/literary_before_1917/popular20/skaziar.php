	<span class="header"><strong>СКАЖИ, АРЕСТАНТ, МНЕ ВСЮ ПРАВДУ...</strong> <br></span><br>
	Скажи, арестант, мне всю правду,<br>
	  За что ты в остроге сидишь?<br>
	  Зачем ты сквозь железну решетку<br>
	  Печально и грустно гляßßдишь?<br>
	  Зачем твои черные глазки<br>
	  Печально на волю глядят?<br>
	  Скажи, арестант, мне всю правду:<br>
	  Ты сколько в разбойниках был?<br>
	  И сколько народу ограбил,<br>
	  И сколько ты душ погубил?<br>
	  - Нет, народу я не грабил,<br>
	  В разбойниках в жизни не был,<br>
	  За красивую, подлую деву<br>
	  Я жизнь человека сгубил.<br>
	  Бывало, окончишь работу<br>
	  И к ней на свиданье идешь,<br>
	  А сам заиграешь гармошку,<br>
	  Веселую песню поешь.<br>
	  Эх, песня моя удалая,<br>
	  Далеко, далеко слышна,<br>
	  Наташа, моя дорогая,<br>
	  Давно ожидает меня.<br>
	  Однажды я в зимнюю пору<br>
	  Пришел я ее навестить.<br>
	  И зимнюю темную ночку<br>
	  С Наташей вдвоем проводил.<br>
	  Пришел раз к ней, что увидел,<br>
	  Ее уж ласкает другой.<br>
	  От ревности сердце забилось,<br>
	  От злости я весь задрожал.<br>
	  НЕ знаю со мной что случилось...<br>
	  Я вынул тут страшный кинжал,<br>
	  И я обозленный, как дикий,<br>
	  К изменнице вдруг подскочил,<br>
	  Ударил могучей рукою,<br>
	  Да в руку кинжал мой вонзил.<br>
	  Просветил луч озаренный,<br>
	  Увидел — соперником брат мой родимый.<br>
	  А он, как статуй, обозленный -<br>
	  Стоял предо мною столбом.<br>
	  Сверкнул тут булатный мой ножик,<br>
	  Я брата родного убил.<br>
	  Тогда я пошел в деревушку<br>
	  Об этом я сам заявил.<br>
	  Я сделал тако преступленье,<br>
	  Я брата и девку убил.<br>
	  Пришли тут тогда понятые,<br>
	  Забрали мальчишку-меня.<br>
	  Осталась семья дорогая,<br>
	  С двумя сыновьями жена.<br>
	  Отец мой тут горько заплакал,<br>
	  В безумьи осталася мать.<br>
	  «Остался, я, мама, навеки<br>
	  И больше мне вас не видать».<br>
	  Окончил рассказ каторжанин,<br>
	  А сам головой покачал,<br>
	  Из глаз покатилися слезы<br>
	  И сам, как дитя, зарыдал.<br>
