 Солнце на закате,<br>
Время на утре.<br>
Сем-ка, девки, на лужок,<br>
Где мы вечером резвились,<br>
В хороводе веселились,<br>
Ну-тко примемтесь опять,<br>
Ту ж игорку продолжать,<br>
Собирались, разыгрались,<br>
На лужку все расплясались<br>
При приятной тишине<br>
Под березонькой одне.<br>
Вдруг стал слышен голосок,<br>
Раздавался во лесок.<br>
«Ах, красавица мил<strong>а</strong>я, <br>
Светик-радость дорогая,<br>
Где ты, где ты? А-а-у!<br>
Без тебя я здесь умру.<br>
Твой глазок меня не видит,<br>
Знать, он нынче ненавидит».<br>
Вдруг все девки спохватились,<br>
И домой все торопились.<br>
Лишь Анютушка-дружок<br>
Тут присела на лужок,<br>
Из цветов венок вила,<br>
Будто милого ждала.<br>
Не успела скласть пучечки,<br>
Как выходит из-за речки<br>
Парень милый, красичок,<br>
Милой Аннушке дружок.<br>
Подошел к речке, прилег,<br>
У Анюты сердце ёк,<br>
Щечки розаном покрылись,<br>
Сердце сердцу покорились.<br>
Ах, любовь, счастье одно<br>
От природы нам дано. <br>
