  <br>
  Песня неизвестного автора с использованием фрагментов нескольких популярных 
  более ранних песен о тройке: первая строка, а также обращение &quot;детина&quot; 
  - вероятно, из «Тройки» Федора Глинки &lt;1825&gt; (см. песню <a href="trojka.htm">«Вот 
  мчится тройка удалая»);</a> последний куплет - из &quot;Тройки&quot; Н. Анордиста 
  <a href="gremzvonok.htm">(&quot;Гремит звонок, и тройка мчится...&quot;,</a> 
  &lt;1839&gt;). Основной текст - вновь сочиненный.<br>
  <br>
  Есть петербургская переработка о шофере, созданная около 1910 года - &quot;На 
  Острова летит стрелою&quot; (поздние варианты - &quot;Летит по Невскому машина&quot;). 
  Несколько ее вариантов см. в статье Владимира Бахтина <a href="../dvor/piter/a-poostrovam.php"> 
  &quot;По островам летел стрелою...&quot;</a> (журнал &quot;Нева&quot;, 1998, 
  № 7, стр. 210-214).<br>
