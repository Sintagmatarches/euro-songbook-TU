  <em>Песня стала популярной с конца XIX - начала XX века. Размер ее и некоторые строки восходят к песне А. Анордиста &quot;Гремит звонок, и тройка мчится&quot; (&quot;Вот на пути село большое&quot;). Один из песенных вариантов (записано А.М. Новиковой в 1919 г. в Тульской области).<br>
