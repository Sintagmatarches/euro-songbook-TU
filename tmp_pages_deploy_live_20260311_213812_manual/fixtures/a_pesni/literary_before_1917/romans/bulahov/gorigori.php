  <br>
  <em>Популярность приобрела обработка В. А. Сабинина. С успехом исполнялся Борисом 
  Штоколовым и Анной Герман. <br>
  <br>
  Антология русского романса. Золотой век. / Авт. предисл. и биогр. 
  статей В. Калугин. - М.: Эксмо, 2006.</em><br>
  <br>
  <br>
  Это первоначальный текст, с которым романс был издан 1868 году. Стихотворение 
  могло быть написано ранее - Чуевский не публиковал своих стихов, все они известны 
  только по нотным изданиям. Нынче романс обычно исполняется с немного измененным 
  текстом. В сборниках встречаются два близких варианта мелодии - видимо, один 
  первоначальный, а другой, более привычный - обработка Сабинина (хотя имя обработчика 
  почти никогда не указывается). В некоторых изданиях и вовсе дается: &quot;музыка 
  неизвестного автора&quot; или &quot;слова неизвестного автора&quot;, хотя оба 
  автора известны.<br>
  <br>
  При повторении в последнем куплете часто поется:<br>
  <br>
  <em>Умру ли я - ты над могилою<br>
  Гори, сияй, моя звезда!</em><br>
  <br>
  <br>
  <strong>Петр Петрович Булахов (1822-1885)</strong>, композитор, родился в семье 
  известного московского оперного певца. К концу жизни оказался парализован, имущество 
  и многие рукописи погибли при пожаре. Последние годы провел в имении Кусково 
  приютившего его графа Шереметева. Романсы писал также его брат Павел Петрович 
  Булахов, оперный тенор.<br>
