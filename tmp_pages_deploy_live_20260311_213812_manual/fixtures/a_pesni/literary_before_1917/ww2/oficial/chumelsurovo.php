Расшифровка фонограммы военных лет<br>
<br>
  <br>
  Песня написана в 1942 году (слова и музыка). Брянские леса были одним из крупнейших 
  партизанских районов Великой Отечественной войны: в них скрывалось 139 отрядов, 
  около 60 тысяч бойцов. Район держался с сентября 1941 года и до освобождения Брянщины 
  Красной Армией (Брянск освобожден 17 сентября 1943 года). <br>
  <br>
  Девять тысяч партизан сложили 
  голову в бою c немецкими войсками и частями РОНА - &quot;Русской Освободительной 
  Народной Армии&quot;.  Это местное фашистское формирование было создано в городке Локоть (тогдашняя Орловская 
  область, на границе с Брянской) в основном, из бывших ссыльных, пострадавших от 
  советской власти. Командовал РОНА Константин Воскобойник, а после его гибели в бою с партизанами -  Бронеслав Каменский. Созданные как милиция, подразделения РОНА были развернуты в 
  штурмовую бригаду в подчинении СС (до 10 тысяч человек), а затем, отступив в 
  Белоруссию, в русскую 29-ю гренадерскую дивизию СС. За карательные операции 
  Каменский получил звание бригаденфюрера и награджден Железным крестом, но осенью 
  1944 года был ликвидирован немцами по подозрению в связях с НКВД. Дивизия, успевшая принять участие в подавление Варшавского восстания, 
  расформирована и влита во власовскую РОА. К боям с регулярными частями, как показал опыт столкновения с Армией Крайовой, РОНА оказалась непригодной. В Варшаве занималась карательными операциями против гражданского населения. <br>
  <br>
  В память о партизанском прошлом вокруг герба Брянска (золотая мортира на красном 
  поле) появился дубовый венок с двумя орденскими лентами - справа ордена Ленина 
  (область награждена им в 1967 г.), слева медали &quot;Партизану Отечественной 
  войны&quot;. Сейчас венок переместился на герб Брянской области (принят 5 ноября 
  1998 г.), а песня стала ее официальным гимном.<br>
