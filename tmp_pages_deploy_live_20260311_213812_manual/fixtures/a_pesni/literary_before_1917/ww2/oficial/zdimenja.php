Русские советские песни (1917-1977). Сост. Н. Крюков и Я. Шведов. 
  М., «Худож. лит.», 1977.</em><br>
 <br>
  <br>
  Стихотворение посвящено кинозвезде Валентине Васильевне Серовой, любимой женщине 
  Константина Симонова. Написано в июле 1941 года на фронте, где Симонов служил военным 
  корреспондентом. Никто из знакомых не верил, что такое стихотворение кто-нибудь согласится опубликовать 
  до окончания войны. Тем не менее Симонов прочитал его по радио, и оно появилось 
  в &quot;Правде&quot; в февраль 1942 года, затем облетело все газеты и вошло в авторский 
  сборник стихов &quot;С тобой и без тебя&quot;, посвященный Серовой. Симонов 
  и Серова стали символом верности, а &quot;Жди меня&quot; - стихийной песней, 
  причем пели ее на произвольный мотив. Затем Матвей Блантер написал музыку (он 
  же написал музыку к <a href="korrespond.php">&quot;Корреспондентской застольной&quot;</a>). 
  <br>
  <br>
  В 1943 году вышел спектакль &quot;Жди меня&quot; по одноименной пьесе Симонова,  
  а режиссеры Александр Столпер и Борис Иванов сняли фильм с тем же названием. И в 
  спектакле, и в фильме главную роль играла Серова; в 1944 году они с Симоновым поженились. 
  &quot;Жди меня&quot; прозвучала также в фильме &quot;Парень из нашего города&quot; 
  (1942) тех же режиссеров по пьесе Симонова &quot;Парень из нашего города&quot;. Оба  фильма снимались в эвакуации в Алма-Ате. <br>
  <br>
  Входила в репертуар Вадима Козина. Леонид Утесов исполнял песню на мелодию Николая 
  Горбенко. На фронте было сложено множество фольклорных &quot;Ответов&quot; на 
  стихотворение. См., например, <a href="../folk/zdutjebja.htm">&quot;Жду тебя, 
  хороший мой...&quot;</a> <br>
