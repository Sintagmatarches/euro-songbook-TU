	<h1>БОЙ ГРЕМЕЛ В ОКРЕСТНОСТЯХ КАБУЛА…</h1>
  <br>
  Бой гремел в окрестностях Кабула, <br>
  Ночь светилась всплесками огня... <br>
  Не сломало нас и не согнуло, — <br>
  Видно, люди крепче, чем броня. <br>
  Мы не дипломаты по призванью – <br>
  Нам милей братишка-автомат, <br>
  Четкие команды приказанья <br>
  И в кармане парочка гранат. <br>
  <br>
        <em>Припев: </em><br>
  <br>
  Вспомним, ребята, мы Афганистан, <br>
  Зарево пожарищ, крики мусульман, <br>
  Грохот автоматов, взрывы за рекой, <br>
  Вспомним, товарищ, вспомним, дорогой. <br>
  Вспомним с тобою, как мы шли в ночи, <br>
  Вспомним, как бежали в горы басмачи, <br>
  Как загрохотал твой грозный АКС, <br>
  Вспомним, товарищ, вспомним наконец. <br>
  <br>
  На костре в дыму трещали ветки, <br>
  В котелке дымился крепкий чай, <br>
  Ты пришел усталый из разведки, <br>
  Много пил, и столько же молчал. <br>
  Синими, замерзшими руками <br>
  Протирал вспотевший автомат <br>
  И о чем-то думал, временами <br>
        Головой откинувшись назад. <br>
  <br>
  Самолет заходит на посадку, <br>
  Тяжело моторами гудя, <br>
  Он привез патроны и взрывчатку – <br>
  Это для тебя и для меня. <br>
  Знайте же, ребята мусульмане, <br>
  Ваша сила в том, что мы за вас. <br>
  И не надо лишних трепыханий, <br>
        В бой ходить нам не в последний раз. <br>
  <br>
        <span class='i'>Антология студенческих, школьных и дворовых песен / Сост. Марина Баранова. 
         М.: Эксмо, 2007.</span><br>
  <br>
  <br>
        Одна из &quot;афганских&quot; (1979-1989) переделок песни <a href="../ww2/folk/baksanskaja.php">&quot;Баксанская&quot;</a>, 
        сочиненной в январе 1943 г. бойцами-альпинистами в районе Эльбруса (там же 
        см. ноты) на мотив предвоенного танго <a href="../cabaret/pustdni.htm">&quot;Пусть 
        дни проходят&quot;</a> (музыка Бориса Терентьева, слова Ильи Финка). Автор переделки - Юрий Кирсанов. <br>
  <br>
        <br>
  <span class="bold">ВАРИАНТ<br>
  <br>
  Над Кабулом снова солнце светит...</span><br>
  <br>
  Над Кабулом снова солнце светит, <br>
  Догорая на закате дня. <br>
  Здесь проходят ночи и рассветы <br>
  По законам смерти и огня. <br>
  Сколько здесь потеряно и смято <br>
  Светлых чувств и нежности земной, <br>
  Но любовь храним мы в сердце свято <br>
  К Родине далекой и родной. <br>
  Что бы ни случилось в жизни нашей, <br>
  Пусть грубеют чувства и сердца, <br>
  Помните, Сережа, Коля, Саша, <br>
  Как мы шли до смерти, до конца. <br>
  <br>
        <em>Припев:</em> <br>
  <br>
  Вспомним, ребята, мы Афганистан, <br>
  Кишлаки и степи, горный океан, <br>
  Эти передряги жизни и войны <br>
  Вспомним на просторах мирной тишины. <br>
  Вспомним обстрелы наших городков, <br>
  Жажду и пекло огненных песков, <br>
  Эти дороги по чужой стране, <br>
  О боях упорных <br>
  Вспомним, как во сне. <br>
  <br>
  В этой жизни мы уже узнали, <br>
  Что такое смерть, огонь и друг, <br>
  Столько раз от грусти уставали <br>
  Без своих девчонок и подруг. <br>
  И пока все призрачно и пусто — <br>
  День не день и ночь не наша ночь, <br>
  Больно и порою сердцу грустно, <br>
  И не знаешь, чем ему помочь. <br>
  Пусть проходят дни, летят недели, <br>
  Есть начало каждого конца, <br>
  Все вернется, все, что мы имели, <br>
        И вернутся верные сердца. <br>
  <br>
  Самолет заходит на посадку, <br>
  И в иллюминаторе — земля, <br>
  Нас встречают ароматом сладким <br>
  Русские цветы и тополя. <br>
  Сердце из груди лететь готово <br>
  На край света, в город дорогой, <br>
  Чтоб теперь припоминать нам снова <br>
  Мимо не прошедшее, как бой, — <br>
  Дорогих друзей своих военных, <br>
  Что остались и которых нет... <br>
  Здесь нас ожидают перемены, <br>
  Но афганских звезд не гаснет свет. <br>
        <br>
        <span class="i">Антология студенческих, школьных и дворовых песен / Сост. Марина Баранова. 
        М.: Эксмо, 2007.</span><br>
