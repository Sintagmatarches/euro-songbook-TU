        <br>
        Навеяна песней  <a href="../dvor/gopsosmykom.php">&quot;Гоп-со-смыком&quot;</a>, пелась на ее мотив. Отклик на войну США во Вьетнаме 1965-75 гг., где использовался американский истребитель-бомбардировщик F-4 Phantom II. СССР 
        с 1965 г. оказывал Северному Вьетнаму военно-техническую помощь, но 
        советские летчики, о которых поется в песне, в войне не участвовали, только 
        расчеты зенитно-ракетных комплексов.   Вошла в альбом Егора Летова &quot;Лет Ит Би&quot; (1989) и альбом группы &quot;Чиж 
        &amp; Co&quot; &quot;Эрогенная зона&quot; (1996).<br>
   <br>
        Есть <a href="../army/fantom-bliz.php">&quot;ближневосточная&quot; переделка</a> 
        о воображаемом конфликте между Израилем и Ираком (&quot;Далеко внизу лежит 
        Багдад...&quot;). Кроме &quot;Фантома&quot; в нем упоминаются истребители-бомбардировщики 
        &quot;Кфир&quot;, которые состояли на вооружении Израиля в 1975-1996 гг.<br>
        <br>
                В 2019 г. под одним из <a href='https://youtu.be/6fySan0NI5E' target="_blank">клипов Youtube с этой песней</a> появился комментарий Валентина Пригарина, который так изложил историю песни:<br>
      <br>
       <span class='i'>Песня (вернее сказать, «песенка») «Мой Фантом» была написана курсантами первого курса Армавирского высшего военного краснознаменного училища летчиков ПВО (АВВАКУЛ) в 1966 году. Она была написана коллективом «авторов» состоящим из 4-5 человек: Сани Шаршавова, Сани Монякина, Кости Колядина, меня и, возможно, еще кого-то – сейчас уже не вспомнить. «Песняк» был написан после отбоя в казарме между вторым и третьим этажами за полчаса и в первоначальном виде состоял из двух куплетов:<br>
<br>
«Мы бежим по огненной земле.<br>
Гермошлем, защелкнув на ходу<br>
Мой «Фантом» с звездою белой, на распластанном крыле,<br>
С рёвом набирает высоту.<br>
<br>
Вижу дыма черную черту.<br>
Мой сосед теряет высоту.<br>
Подо мною Эдвард с Бобом пронеслись на встречу с богом.<br>
Ноль семнадцать вижу на борту».<br>
<br>
И это было все – больше ничего не было, единственные куплеты. Среди «писателей» было три гитариста-любителя – Саня Шаршавов, Саня Монякин и Костя Колядин, и они всё это быстро перекатали в американский рок-н-ролл, название которого я забыл, поэтому не смог сейчас найти его в Ю-Тьюбе – но первоначальная мелодия «песняка» была именно той. Кто придумал остальные куплеты, мы, бывшие полвека назад курсантами, уже вспомнить не смогли, хоть и пытались – слишком давняя история…<br>
<br>
О теперешних попытках оживить «одряхлевшую память» и вспомнить как всё было вы можете прочитать здесь:<br>
<br>
<a href ='http://avvakul.ru/forum/viewtopic.php?f=28&t=378&start=19240' target="_blank">http://avvakul.ru/forum/viewtopic.php?f=28&t=378&start=19240</a><br>
<br>
Если что неясно, могу объяснить, что т.н. «понинцы» - это наша курсантская рота (впоследствии ставшая авиационным полком этого же училища). Называлась она так по имени командира роты по кличке «Пони».<br>
<br>
Форумный ник «Мао» - это Славка Моисеев, «Бублик» - Володя Бублейник, «Aviator 46» - Виталик Макаров, «ВАГ» - Валерка Горячев, «Грек» - Мишка Гречишкин, «Ходжа» - Саид Насретдинов, а «510-й» - это я (училищная кличка «Ван»). Все в прошлом офицеры и лётчики-истребители. Это я к тому, что врать мы не собираемся – рассказали, как было. Честно говоря, никто не думал, что песняк будет иметь столь долгую жизнь. Ведь это не «высокая поэзия»…, да и не «поэзия» вообще, положа руку на сердце. Поэтому я был очень удивлен, когда через десяток лет услышал ее в электричке – какая-то компания пела ее под гитару. Спустя ещё пару десятков лет, читая Пелевина («Омон Ра», по-моему) я нашёл кусочек «песняка» и там. ЗдОрово, подумал – еще кто-то помнит. Потом увидел эту песню уже по телеку, году в 2002 в исполнении «Чиж и К», в документальном фильме «Вьетнам, секрет победы», ТВЦ. Там был, кстати, неплохой видеоряд – без фальшивки. Что означает, что там показали фейсецы сбитых американцев и самолёты именно Вьетнамской войны: их истребители F-4 «Фантом» и F-105 «Тандерчиф», их же штурмовики А-4 «Скайхок», А-6 «Интрудер» и А-7 «Корсар», а также наши истребители «МиГ-17» и «МиГ-21» – всё нормально.<br>
<br>
А то в некоторых роликах есть даже «МиГ-15», которых уже давно не было, «МиГ-23» и даже «МиГ-29», до появления которых было ещё очень далеко. Если уж делать видеоряд, то лучше использовать то, что там на самом деле летало – это просто справедливо.<br>
<br>
Самый забойный ролик со стареньким «МиГ-17» – вот этот : http://www.youtube.com/watch?v=-uhvr7AKS1M&feature=related [ссылка не работает]<br>
<br>
Из училищных «гитаристов» никто профессиональным так и не стал. За одним исключением – Александр «Маршалл», хотя он учился значительно позже нас – лет на пятнадцать. Этот случай мне запомнился, так как его отец – Виталий Павлович Миньков был непосредственно моим командиром эскадрильи с 1969 по 1972 год (когда я сам был уже лётчиком-инструктором). Он был не просто комэском, а лучшим комэском в мире. <br><br>
А если уж песняк считается «народным», что ж, пусть… Мы, «армавирцы» выпуска 1969, доживающие свой век и разбросанные по всему миру – от Канады и Калифорнии до Хабаровска и Норвегии, не возражаем, а даже наоборот, очень приветствуем. И спасибо тем, кто эту песенку ещё исполняет. Но, если хотите, можно упомянуть, что автором слов был, прежде всего, Саня Шаршавов (на одиночном фото), хотя придумывали её вместе человек четыре, как я уже рассказывал. Но Саня сразу после выпуска (марте 1970-го) погиб на перехватчике «Су-9» на Севере – его нашли в Баренцевом море только через несколько недель после катастрофы – а в наш полк сообщили шифровкой. Отличный был парень.<br>
<br>
Так что пусть уж будет автором он. И в память о нём, да и по праву....</span><br>
