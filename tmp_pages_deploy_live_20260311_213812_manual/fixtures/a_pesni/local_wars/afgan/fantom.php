        Я снова по чужой земле иду,<br>
        Гермошлем застегнут на ходу.<br>
        Мой &quot;Фантом&quot;, как ястреб быстрый,<br>
        В небе голубом и чистом<br>
        С ревом набирает высоту. <br>
        <br>
        Вижу в небе алую черту,<br>
        Это МиГ-17 на хвосту.<br>
        Вижу я как Ричард с Бобом<br>
        Понеслись на встречу с гробом,<br>
        Мой &quot;Фантом&quot; теряет высоту.<br>
        <br>
        Ну а дома ждет меня жена,<br>
        Может быть, сейчас с другим она.<br>
        Мы воюем во Вьетнаме,<br>
        С узкоглазыми скотами.<br>
        Нам победа здесь не суждена.<br>
        <br>
        Я ползу по выжженной земле,<br>
        Молодым обидно умирать.<br>
        Будут матери молиться,<br>
        Будут лбом о землю биться,<br>
        Снова заставляют убивать.<br>
        <br>
        На допросе только я спросил:<br>
        &quot;Кто ж пилот, который меня сбил?&quot;<br>
        И ответил мне раскосый,<br>
        Что командовал допросом:<br>
        &quot;Сбил тебя наш летчик Син Ин Цин&quot;.<br>
        <br>
        Все ты врешь, вьетнамская свинья,<br>
        В гермошлеме четко слышал я:<br>
        &quot;Ты Петро меня прикроешь,<br>
        А Иван - &quot;Фантом&quot; накроешь&quot;<br>
        Сбил меня советский ас Иван.<br>
        <br>
        Где-то далеко родной Техас,<br>
        Там мои живут отец и мать.<br>
        Мой &quot;Фантом&quot; как зверь убитый,<br>
        На земле лежит разбитый,<br>
        Мне на нем уж больше не летать.<br>
        <br>
