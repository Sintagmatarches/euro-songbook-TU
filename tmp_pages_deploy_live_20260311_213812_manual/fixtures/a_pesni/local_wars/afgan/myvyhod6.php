<h1>ПЕСНЯ ИЗРАИЛЬСКИХ ДЕСАНТНИКОВ</h1>
      
         Мы выходим на рассвете,<br>
Из Синая дует ветер,<br>
Подымая тучи пыли до небес.<br>
Позади страна родная,<br>
Впереди пески Синая,<br>
На груди мой автомат наперевес.<br>
<br>
Танки мчатся перед нами,<br>
Извергая смерть и пламя,<br>
От разрывов содрагается земля.<br>
Миражи над нами вьются,<br>
И сердца сильнее бьются<br>
От того, что началась уже война.<br>
<br>
Мы прошли пески Синая,<br>
Вышли к берегу канала,<br>
И свободен стал опять наш Шарм-Аль-Шейх.<br>
Зеленеть пустыни будут, <br>
Но героев не забудут,<br>
О героях будут вечно песни петь....<br>
<br>
<span class='i'>С сайта <a href="http://www.rusisrael.com/lyrics/47563/pesnja-izrailskih-desatnikov/">rusisrael.com</a>, 
  вывесил <a href="http://www.rusisrael.com/user/49378/pushkin/">Пушкин</a> 1 
  июля 2007, с прим.:<br>
  <br>
&quot;Эту песню мне пел папа, как колыбельную. Как и предупреждали соседи, 
  из меня таки вырос &quot;Израильский агрессор&quot;... :-)<br>
  <br>
У этого произведения интересная история. Когда мой отец был студентом, летом 
  69 года он ездил в Ригу, там был “слет” еврейской-сионистской молодежи. И такие 
  у них были “комсомольские песни”. Вы только вдумайтесь! Советский Союз, 1969 
  год и сионистская молодежь с такими песнями! Люди там, конечно, были разные, 
  в-основном, наверно, конечно, “ботаники”, хотя, конечно, распевать такие песни 
  в 69 году уже круто... Но были ребята и покруче, они поставили себе цель уехать 
  в Израиль любым способом и служить в Цахале, и обязательно в десантных войсках. 
  Для этого они готовились: учили иврит и много тренировались... А потом совершили 
  попытку вооруженного угона самолета (неудачную, у КГБ везде были свои агенты)...&quot;</span><br>
          <br>
           <br>
           
            Реакция советских евреев на Шестидневную войну (5-10 июня 1967), в ходе которой Израиль нанес поражение коалиции арабских стран и расширил территорию, но Египет сохранил контроль над Синайским полуостровом. Переделка <a href="../bard/chabuckij/marchin.php">&quot;Марша 
        иностранного легиона&quot;</a> барда Сергея Шабуцкого, написанного в 1964 
        г.:<br>
