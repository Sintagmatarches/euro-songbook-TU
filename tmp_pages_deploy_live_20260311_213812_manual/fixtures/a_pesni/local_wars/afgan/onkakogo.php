	<h1>ОН КАКОГО ЦВЕТА?..</h1>
<br>
Он какого цвета? <br>
Он на карте — пестрый. <br>
Алые ракеты <br>
Мерят наши версты. <br>
<br>
Он какого цвета? <br>
Он на карте рыжий. <br>
За Кабулом где-то <br>
Гром Пандшера слышен. <br>
Он какого цвета? <br>
Он на карте рыжий. <br>
За Кабулом где-то <br>
Гром Пандшера слышен. <br>
<br>
БТР зеленый, <br>
Сзади пыль клубится. <br>
Зноем опалены, <br>
Желты наши лица. <br>
<br>
Тельник полосатый, <br>
Улицы — опасны. <br>
Черны автоматы, <br>
Пули тёмно-красны. <br>
Тельник полосатый, <br>
Улицы — опасны. <br>
Черны автоматы, <br>
Пули — темно-красны. <br>
<br>
Черной сажей мажется <br>
Наш уют осенний, <br>
И багровым кажется <br>
Пакистан соседний. <br>
<br>
С пулею, раскрученной <br>
В седине виска, <br>
Друг ладонью скрюченной <br>
Ловит горсть песка... <br>
С пулею, раскрученной <br>
В седине виска, <br>
Друг ладонью скрюченной <br>
Ловит горсть песка... <br>
<br>
А декабрь тут синий, <br>
Руки стужен вяжет. <br>
На виски вдруг иней <br>
Сединою ляжет. <br>
<br>
Ждем весну зеленую — <br>
В дом свой возвратиться, <br>
Чтоб водой студеною <br>
У крыльца умыться. <br>
Ждем весну зеленую — <br>
В дом свой возвратиться, <br>
Чтоб водой студеною <br>
У крыльца умыться. <br>
<br>
Алы губы женские, <br>
Что нас ждут в России, <br>
Омские, смоленские — <br>
Нету их красивей... <br>
<br>
Так какие ж краски <br>
Чаще здесь встречаются? <br>
Жизнь тут быль — не сказка, <br>
С нами не кончается. <br>
