	<h1>ЗОВУТ В АФГАНСКОЙ СТОРОНЕ…</h1>
<br>
Зовут в афганской стороне <br>
Нас непривычно — шурави. <br>
А в наших снах, как облака, <br>
Летят, курлычут журавли. <br>
Сидим за дружеским столом, <br>
Когда кончается работа, <br>
И екнет сердце у друзей, <br>
Когда опаздывает кто-то. <br>
<br>
        <em>Припев: </em><br>
<br>
Пожелай, пожелай, командир, <br>
Чтоб любимая не разлюбила, <br>
Чтобы в памяти чьей-то всегда <br>
Наше место не занято было, <br>
Чтобы нас ожидала семья <br>
И чтоб Родина нас не забыла. <br>
Чтобы нас ожидала семья <br>
И чтоб Родина нас не забыла. <br>
<br>
И вновь звучит приказ: «Вперед!», <br>
И Ми-восьмой готов для взлета, <br>
Берем побольше мы гранат <br>
Для предстоящего налета. <br>
<br>
        <em>Припев: </em><br>
<br>
Пожелай, пожелай, командир, <br>
Чтоб удача нам не изменила, <br>
Чтобы в памяти чьей-то всегда <br>
Наше место не занято было, <br>
Чтобы нас ожидала семья, <br>
И чтоб Родина нас не забыла. <br>
Чтобы нас ожидала семья, <br>
И чтоб Родина нас не забыла. <br>
