        &quot;Di szwue&quot; (&quot;Присяга&quot; или &quot;Клятва&quot;) или 
        &quot;Di naje szwue&quot; (&quot;Новая присяга&quot;) - гимн Бунда, союза 
        еврейских рабочих Польши, Литвы и России, а также партии гимн партии Poale-Syjon. 
        Песня создана в 1902 году к пятилетию Бунда. Сменила более раннюю песню 
        с тем же названием &quot;Di szwue&quot; (или &quot;Di ałte szwue&quot; 
        - &quot;Старая присяга&quot;).<br>
        <br>
        История &quot;Di szwue&quot; начинается в 1892 году. В прикарпатском городке 
        Коломыя (современная Ивано-Франковская область Украины) забастовали четыре 
        сотни ткачей. Тогда впервые была засвидетельствована клятва на Торе еврейских 
        рабочих в том, что они не отступят и не начнут работу, пока требования 
        не будут выполнены. Через семь недель забастовку пришлось прикратить из-за 
        угрозы голодной смерти. Раввин освободил рабочих от клятвы. <br>
        <br>
        Через несколько лет клятва еврейских рабочих стала исполняться в форме 
        песни с библейскими мотивами (&quot;Di ałte szwue&quot; - &quot;Wi hejlik 
        iz di natur...&quot;). Ее авторы неизвестны. Первое свидетельство об исполнении 
        относится к 1897 году, когда в местечке Крынки Белостокского округа забастовало 
        около 300 кожевников, евреев и христиан. Христиане дали присягу на кресте, 
        евреи - на тфилине, что не уступят и сохранят солидарность. После этого 
        евреи запели &quot;Di szwue&quot;. Песня имела широкую популярность в 
        еврейской среде Польши и черте оседлости Российской империи, известы разные 
        ее варианты. Впервые издана в Белостоке в начале XX века на листовке Бунда.<br>
        <br>
        Осенью 1902 года в эмигрантских кругах Бунда в Берне (Швейцария) известный 
        еврейский этнограф и писатель Ш. А. Ан-ский (Sz. A. An-ski, настоящее 
        имя Salomon Zainwil Rappaport, 1863-1920), участник русского народнического 
        движения и в 1894-1900 гг. секретарь Петра Лаврова в Париже, сложил текст 
        &quot;Новой присяги&quot; (&quot;Di naje szwue&quot;). Вместе с другим 
        стихотворением Ан-ского &quot;Cum Bund&quot; текст был опубликован под 
        псевдонимом Z. Sinani в 14-м номере лондонского журнала &quot;Der Idiszer 
        Arbajter&quot; в октябре 1902 года. <br>
        <br>
        Ан-ский формально никогда не состоял в Бунде, но симпатизировал ему. Музыку 
        к тексту скомпоновал Г. Бецк (G. Beck), дирижер бундовского и других социалистических 
        хоров в Лондоне. По национальности Бецк был латышским немцем, но долго 
        работал в еврейской среде и хорошо знал идиш. Мелодия сборная - заимствовала 
        мотивы российские (<a href="../popular20/kolslaven.htm">&quot;Коль славен 
        наш Господь в Сионе...&quot;</a>) и немецкие. Уже 1 мая 1903 новая песня 
        была повсеместно популярна в кругах Бунда: ее пели и Польше, и в Литве, 
        и в России. Формально в Польше была нелегальной вплоть до 1926 года, хотя 
        исполнялась открыто. В 1926 году при попытке издать восьмитомное собрание 
        сочинений Ан-ского цензура запретила восьмой том, где на стр. 145 должна 
        была быть напечатана эта песня. Страница была напечатана пустой, с одним 
        заголовком без текста. Но затем издатели все же выиграли апелляционный 
        суд, запрет цензуры был отменен, и с тех пор песня стала легальной.<br>
