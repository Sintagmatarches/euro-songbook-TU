  <br>
  <strong>Василий Иванович Богданов</strong> (1837-1886) - морской врач. В 1866-1867 
  совершил кругосветное путешествие на клипере &quot;Изумруд&quot;. С 1863 года 
  сотрудничал в сатирическом журнале &quot;Искра&quot; (журнал закрыт в 1873 году).<br>
