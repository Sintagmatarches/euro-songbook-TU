  <strong>2. ДУБИНУШКА</strong><br>
  <br>
  Слова Александра Ольхина <br>
  <br>
  Много песен слыхал я в родной стороне, <br>
  &lt;Там&gt; про радость и горе в них пели;<br>
  Из всех песен одна в память врезалась мне, <br>
  Это песня рабочей артели:<br>
  Ой, дубинушка, ухнем!<br>
  Ой, зеленая, сама пойдет! <em>(два раза)</em><br>
  Подернем! <em>(два раза)</em> Ух! <br>
  <br>
  И от дедов к отцам, от отцов к сыновьям<br>
  Эта песня идет по наследству,<br>
  И лишь только как станет работать невмочь,<br>
  Мы к дубине, как к верному средству.<br>
  Ой, дубинушка <em>и т. д.</em><br>
  <br>
  Говорят, что мужик наш работать ленив,<br>
  Пока не взбороздят ему спину,<br>
  Ну, так как же забыть наш родимый мотив<br>
  И не петь про родную дубину.<br>
  Ой, дубинушка <em>и т. д.</em><br>
  <br>
  Англичанин-хитрец, чтоб работе помочь,<br>
  Изобрел за машиной машину,<br>
  А наш русский мужик, коль работа невмочь,<br>
  Так затянет родную дубину.<br>
  Ой, дубинушка <em>и т. д.</em> <br>
  <br>
  Тянем с лесом судн<u>о</u>, иль железо куем, <br>
  Иль в Сибири руду добываем, <br>
  С мукой, с болью в груди одну песню поем, <br>
  Про дубину в ней всё вспоминаем.<br>
  Ой, дубинушка <em>и т. д.</em> <br>
  <br>
  И на Волге-реке, утопая в песке, <br>
  Мы ломаем и ноги, и спину, <br>
  Надрываем там грудь и, чтоб легче тянуть <br>
  Мы поем про родную дубину.<br>
  Ой, дубинушка <em>и т. д.</em> <br>
  <br>
  Пускай мучат и бьют, пускай в цепи куют,<br>
  Пусть терзают избитую спину, -<br>
  Будем ждать и терпеть, и в нужде будем петь<br>
  Всё про ту же родную дубину.<br>
  Ой, дубинушка <em>и т. д.</em> <br>
  <br>
  Мы пируем при блеске огней на балах,<br>
  И шутя мы поем про дубину,<br>
  А забыли о тех, кто сидит в кандалах<br>
  Всё за ту же родную дубину.<br>
  Ой, дубинушка <em>и т. д.</em> <br>
  <br>
  Но ведь время придет, и проснется народ, <br>
  Разогнет он избитую спину, <br>
  И в родимых лесах на врагов подберет<br>
  Здоровее и крепче дубину. <br>
  Ой, дубинушка, ухнем!<br>
  Ой, зеленая, сама пойдет! <em>(два раза)</em><br>
  Подернем! <em>(два раза)</em> Ух!<br>
  <br>
  &lt;1885&gt;<br>
  <br>
  <em>«Общее дело». Женева, 1885, № 80, дек., без подписи, в рубрике 
  «Из запретных стихотворений последних лет» (ст. 2 восстановлен по другим печ. 
  источникам и спискам). «Отголоски революции». Таганрог, 1886, с вар. и четырьмя 
  новыми строфами (текст см. в изд.: «Вольная русская поэзия второй половины XIX 
  века» / Вступ. статья С. А. Рейсера и А. А. Шилова. Л., 1959 (Б-ка поэта, БС)); 
  «Новый сборник революционных песен и стихотворений». Париж, 1898. <strong>Ольхин</strong> Александр Александрович (1839-1897) - либеральнвй попутчик революционных народников 1870-х годов, он в 1865 г. оставил государственную службу. В качестве участкового мирового судьи обратил на себя внимание «тенденциозностью» своих решений. Выступления в качестве защитника по процессам нечаевцев, делу В.М. Дьякова, о демонстрации на Казанской площади в Петербурге в 1876 
  г., на процессах «50-ти» и «193-х» обеспечили ему симпатии революционных групп и передовой части русского общества. Не входя в «Землю и волю», он оказывал партии ряд важных услуг и сотрудничал в изд. «Начало» и «Земля и воля».
В справке о нем, составленной в департаменте полиции, сообщалось, что он с 1878—1879 
  гг. стал «появляться в самых темных кружках, знаться с подонками общества, и 
  в этой темной среде читал и пел революционные песни, иногда даже сочинял их» 
  (Кравчинский С.М. Смерть за смерть: Убийство Мезенцева. Пг., 1920. С. 9 — из 
  вступ. статьи В. Петровского). После покушения А.К. Соловьева на Александра 
  II, он в июле 1879 г. был выслан в Вологодскую, а в 1880 г. — в Пермскую 
  губернию. Подробнее о нем см. в изд.: «Поэты-демократы 1870—1880-х годов» 
  / Вступ. статья Б.Л. Бессонова; биогр. справки, подготовка текста и примеч. 
  В.Г. Базанова и др. Л., 1968. (Б-ка поэта, БС). С. 502—503. Ст-ние в различных вар. было широко популярно уже в начале 1960-х годов. См., например, рассказ о «монастырской» и «нецензурной» «Дубинушках», 
  распространенных в Екатеринославской губернии (Достоевский А.М. Воспоминания. 
  Л., 1930. С. 280). Вар. революционной «Дубинушки», относящийся к середине 
  1870-х годов, приводит В.Г. Короленко в «Истории моего современника» (кн. 2, 
  ч. 3, гл. 4). Еще один интересный вариант см. в «Отечественных записках». 1871, 
  № 7. Ряд вар. собрал В.А. Слепцов (см.: «Литературное наследство». 1963. 
  Т. 71. С. 429). Из позднейших переделок отметим популярную «Машинушку», подражание «Дубинушке», см.: «Пролетарские поэты». Л., 1935. Т. 1. С. 43-44 (Б-ка поэта, БС). «Дубинушка» многократно перепечатывалась в различных сб., 
  распространялась в рукоп. и гектографированных копиях и была одной из наиболее 
  популярных песен русского революционного репертуара до самой Октябрьской революции. 
  Текст А.А. Ольхина восходит к «Дубинушке», принадлежащей перу В.И. Богданова (1838-1886), впервые напечатанной в «Будильнике» (1865, №60). В переработке Ольхина сохранены с некоторыми вар. три строфы Богданова. Припев - во всех вар. «Дубинушки» приблизительно одинаковый - представляет собою вар. распространенной бурлацкой песни, восходящей, в свою очередь, к старинной трудовой песне о корчевке деревьев; ср. полный текст припева: А&lt;нненко&gt;в П.В. Провинциальные письма // «Современник». 
  1849, № 8. С. 235 и Корнилов И.П. Волжские бурлаки // «Морской сб.». 1862, 
  № 7. С. 17. Переделка Ольхиан значительно усилила революционную направленность ст-ния. Авторство Ольхина со значительной степенью вероятности утверждается как революционной традицией: таки агентурной справкой департамента полиции: «Сочинил переделку &quot;Дубинушки&quot; в самом возмутительном духе и просил доставлять ему песни, поющиеся между фабричными, чтобы переделывать их в революционные» (цит. по предисл. В. Петровского к изд.: Кравчинский С.М. Смерть за смерть: Убийство Мезенцева. Пг., 1920. С. 9-10). Историю текста «Дубинушки» и ее музыкальной интерпретации см. в книге Е. В. 
  Гиппиуса («Эй, ухнем!», «Дубинушка»: История песен. М. 1962). <br>
  <br>
  Вольная русская поэзия XVIII-XIX веков. Т. 2. Сост., подг. текста и примеч. С.А. Рейсера. Л.: Сов. писатель, 1988. (Б-ка поэта. Большая 
  сер.). №219. С. 317-318. Примечания, с. 620-621.</em><br>
