	<span class="header"><strong>ДУБИНУШКА</strong><br></span>
  <br>
  Существует много вариантов революционной &quot;Дубинушки&quot;: авторский текст 
  Василия Богданова, опубликованный в 1865 году, основанный на нем авторский текст Александра 
  Ольхина, опубликованный в 1885 году (именно он получил большое распространение в подпольной 
  печати), а также анонимные и авторские варианты, в том числе дальнейшие переработки текста 
  Ольхина. Общепринятого канонического текста у песни нет. 
  <br>
  <br>
  В дальнейшем &quot;Дубинушка&quot; послужила основой для других революционных 
  песен. Бытовала, например, &quot;Студенческая Дубинушка&quot; (&quot;Много песен 
  слыхал я в Бутырской тюрьме...&quot;). На основе варианта Ольхина в конце 1890-х 
  гг. была создана <a href="machinuchka.htm">&quot;Машинушка&quot;</a>, которая 
  постепенно вытеснила &quot;Дубинушку&quot; и стала одной из самых популярных 
  песен первой русской революции (1905-1907), а сейчас практически забыта (ее 
  вероятным автором был молодой Лев Троцкий). Напротив, &quot;Дубинушку&quot; 
  поют до сих пор.<br>
  <br>
  Мелодия напоминает мотив 
  кандальной песни <a href="../popular20/kogda_na.htm">&quot;Лишь только в Сибири 
  займется заря&quot;</a> (он использован также в красноармейской песне <a href="../grvojna/narod/tamvdali.php">&quot;Там, 
  вдали за рекой&quot;</a>). Припев заимствован из традиционных бурлацких песен, 
  восходящих, в свою очередь, к старинной трудовой песне о корчевке деревьев. 
  (см. <a href="../bezzem/ejuhnem.htm">&quot;Эй, ухнем!..&quot;</a>, <a href="../bezzem/davyrebiata.htm">&quot;Да 
  вы, ребята, бери дружно!&quot;</a>. Шутники говорят, что припев &quot;Подернем, 
  подернем...&quot; заимствован вовсе не из трудовой, а из похабной песни и намекает 
  на процесс размножения - но это антинаучно). Фольклор бурлаков послужил также 
  основой для песни Дмитрия Клеменца и Сергея Синегуба <a href="barka.htm">&quot;Барка&quot;</a> 
  (опубл. в 1873).<br>
  <br>
  Припев использован в одном из мультфильмов о Чебурашке (&quot;Старуха Шапокляк&quot;): 
  Шапокляк едет по рельсам на дрезиние и напевает: &quot;Эх, дрезинушка, ухнем! 
  Эх, зеленая, сама пойдет!..&quot;<br>
  <br>
  Заглавие &quot;Дубинушка&quot; имеет также песня чайковца И. А. Богданова (1873), 
  основанная на &quot;Барке&quot; (см. <a href="barka.htm">&quot;Барка&quot;</a>), 
  но она, видимо, никогда не исполнялась - известна только по дневниковой записи 
  автора.<br>
  <br>
  Переработки песни делаются по сей день - см., например, неофициальный гимн советских 
  (а затем и современных российских) студентов-физиков <a href="../stud/dubina.php">&quot;Дубина&quot;</a> 
  (Тот, кто физиком стал...).<br>
