  <strong> 1. Дубинушка</strong><br>
  <br>
  Слова Василия Богданова <br>
  <br>
  Много песен слыхал я в родной стороне, <br>
  Как их с горя, как с радости пели,<br>
  Но одна только песнь в память врезалась мне, <br>
  Это - песня рабочей артели:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  За работой толпа, не под силу ей труд,<br>
  Ноет грудь, ломит шею и спину…<br>
  Но вздохнут бедняки, пот с лица оботрут<br>
  И, кряхтя, запевают дубину:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  Англичанин-хитрец, чтоб работе помочь,<br>
  Вымышлял за машиной машину;<br>
  Ухитрились и мы: чуть пришлося невмочь,<br>
  Вспоминаем родную дубину:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  Да, дубинка, в тебя, видно, вера сильна,<br>
  Что творят по тебе так поминки,<br>
  Где работа дружней и усердней нужна,<br>
  Там у нас, знать, нельзя без дубинки:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  Эта песня у нас уж сложилась давно;<br>
  Петр с дубинкой ходил на работу,<br>
  Чтоб дружней прорубалось в Европу окно, -<br>
  И гремело по финскому флоту:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  Прорубили окно… Да, могуч был напор<br>
  Бессознательной силы… Все стали <br>
  Эту силу ценить и бояться с тех пор…<br>
  Наши ж деды одно напевали:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  И от дедов к отцам, от отцов к сыновьям<br>
  Эта песня пошла по наследству;<br>
  Чуть на лад что нейдет, так к дубинушке там<br>
  Прибегаем как к верному средству:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  Эх, когда б эту песню допеть поскорей!<br>
  Без дубины чтоб спорилось дело<br>
  И при тяжком труде утомленных людей<br>
  Монотонно б у нас не гудело:<br>
  «Ухни, дубинушка, ухни!<br>
  Ухни, березова, ухни!<br>
  Ух!..»<br>
  <br>
  &lt;1865&gt;<br>
  <br>
  <em>«Будильник», 1865, № 60<br>
  <br>
  Русская поэзия XIX - начала XX в. / Сост., вступ. статья, примеч. Н. Якушина. 
  - М.: Худож. лит., 1987. (Б-ка учителя).</em><br>
