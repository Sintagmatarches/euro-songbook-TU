    <em>Из песенника середины 1990-х годов</em><br>
    <br>
    <br>
    Песня на основе стихотворения Филиппа Шкулева, написанного в декабре 1905 
    или в 1906 году в связи с баррикадными боями на Пресне, в которых участвовал 
    Шкулев. <br>
	<br>
	Фольклорист Владимир Бахтин указывает, что мотив взят у шансонетки &quot;Мы шансонетки, стреляем метко&quot; (Жили-были... Разговоры с В.С. Бахтиным / Сост. О.А. Комарова. - СПб.: Композитор Санкт-Петербург, 2006, с. 22).  Кстати, в начале 1930-х годов РАПМ (Российская ассоциация пролетарских музыкантов)  требовала запретить 
    песню, заявляя, что она сложена на мотив венской шансонетки. <br>
	<br>
    Поздние советские источники авторами мелодии называли разных лиц: Скворцова, руководившего любительским оркестром в 
    московских чайных (Поэзия в большевистских изданиях 1901-1917 / Вступ. статья, 
    сост., подг. текста и примеч. И. С. Эвентова. Л., Сов. писатель, 1967 (Б-ка 
    поэта), с. 441); студента, впоследствии 
    советского академика Н. Остроградского (<a href="../dvor/moskva/a-karimov.php">И. 
    Каримов. Московский клуб самодеятельной песни и его история</a>, 1979); Я. Озолина (ряд песенников эпохи застоя). <br>
    <br>
	    <br>
    <img src="chkulev.jpg" alt="Филипп Шкулев" width="193" height="287"><br>
    <br>
    <strong>Шкулев Филипп Степанович</strong> (1868-1930), родился в дер. Печатники 
    Московской губ. в семье крестьянина. Подростком поступил на одну из московских 
    ткацких фабрик, где машиной ему оторвало руку. После двухлетнего пребывания в больнице 
    работал подручным у овощного торговца в Москве. Начал писать стихи, с 1890-х 
    активно печатался. Совместно с Максимом Леоновым организовал революционное издательство 
    &quot;Искра&quot;, выпускал брошюры, песенники и периодические издания, за 
    что получил полгода тюрьмы. В советский период вступил в компартию, занимался 
    литературной деятельностью. <br>
