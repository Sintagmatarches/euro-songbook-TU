<h1>УГРЮМЫЙ ЛЕС СТОИТ СТЕНОЙ КРУГОМ...</h1>

  Угрюмый лес стоит стеной кругом; <br>
  Стоит, задумался и ждет. <br>
  Лишь вихрь порой в его груди взревет. <br>
  Вперед, друзья, вперед, вперед, вперед. <br>
  <br>
  В глубоких рудниках металла звон, <br>
  Из камня золото течет... <br>
  Там молотом своим он в камень бьет, — <br>
  Вперед, друзья, вперед, вперед, вперед. <br>
  <br>
  Иссякнет кровь в груди его златой, <br>
  Железа ржавый стон замрет... <br>
  Но в недрах глубоко земля поет: <br>
  Вперед, друзья, вперед, вперед, вперед. <br>
  <br>
  Кто жизнь в бою неравном не щадит, <br>
  С отвагой к цели кто идет, <br>
  Пусть знает: кровь его тропу пробьет, — <br>
  Вперед, друзья, вперед, вперед, вперед. <br>
  <br>
