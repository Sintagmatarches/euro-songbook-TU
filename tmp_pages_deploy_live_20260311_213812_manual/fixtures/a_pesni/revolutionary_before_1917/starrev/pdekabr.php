Истории песни посвящена статья Романа Лейбова "Кровь его тропу пробьет":
    эпизод из истории бытования советского песенного наследия /
    <a href="https://ufajournal.ranepa.ru/issues/2020/3-4" target="_blank">Фольклор и антропология города. 2020. Т. III. №3-4</a>. С. 49-68.<br>

<br>
  Песня основана на стихотворении Александра Вермишева "Вперед",
опубликованном в легальном журнале бакинских большевиков "Современная жизнь", 1911, №3,
    22 апреля, под псевдонимом "ша": <br>
    <br>
    <div class="bold">Вперед</div>
    <br>
    В глубоких шахтах цепи звон —<br>
    Из камня золото течет,<br>
    Мятежный молот в скалы бьет:<br>
    «Мы долго шли вперед, вперед».<br>
    <br>
    Угрюмо лес стеной кругом<br>
    Стоит, задумался и ждет…<br>
    Порою вихрем донесет:<br>
    «Смелей, борцы, вперед, вперед!»<br>
    <br>
    Иссякнет золото, и стон<br>
    Железа в руднике замрет,<br>
    Лишь эхо под землей поет:<br>
    «Они ушли вперед, вперед».<br>
    <br>
    Белеют по холмам кресты…<br>
    Тайга их тайны стережет…<br>
    И длинный ряд могил ведет<br>
    Через тайгу — вперед, вперед…<br>
    <br>
    И те, кто, смерти не боясь,<br>
    На эшафот взойдут, смеясь,<br>
    Пусть помнят: кровь их цепь порвет<br>
    И путь пробьет — вперед, вперед! <br>
    <br>
    <div class="i">Пролетарские поэты. Т. II: 1911–1914 / Сост. Г.Д. Владимирский. [Л.]: 1936. (Б-ка поэта. Большая сер.). С. 16. -
    Без разбивки на строфы</div>
    <br>
    <br>
    Александр Александрович Вермишев (1879–1919) - выпускник Бакинской гимназии и Юрьевского университета, большевик-подпольщик,
    участник Октябрьского переворота 1917 г., затем заведующий Петроградским отделом транспорта и член Петроградской
    центральной уголовной следственной комиссии. На Гражданской войне - в Красной армии,
    комиссар и корреспондент РОСТа; погиб на фронте. <br>
    <br>
    По мнению Р. Лейбова, образцом для Вермишева было стихотворение Федора Тютчева "Из края в край, из града в град" (1830-е),
    а мелодия сложилась под влиянием песни <a href="../popular20/vot3potch.php">"Вот мчится тройка почтовая".</a>
    Неизвестно, когда на основе стихотворения появилась песня - возможно, вскоре после публикации,
    до 1917 г., но широкого распространения не получила и бытовала в локальной социал-демократической среде.
Ее изданий в песенниках и упоминаний о песенном бытовании не зафиксировано до 1937 г., когда она была издана с нотами
в сборнике "Четыре песни каторги. В обработке для голоса и фортепьяно А. Пащенко" (Л.: Музгиз, 1937) под заголовком
    "Угрюмый лес. Старая революционная песня" и пометкой "Мелодия по записи Н. Моньякова".
    В песне размер иной, чем у стихотворения: первая, третья и четвертая строки каждого четверостишия
удлинились на два слога.
    <br>
    <br>
К 1960 г. песня вошла в репертуар Омского государственного русского народного хора. Видимо, тогда же ее начала петь
    капелла Томского университета (бывший участник капеллы, увидев текст, сказал редактору a-pesni, что пел ее в капелле).
    От Омского хора заимствована Хором имени Пятницкого и записана им на пластинку. Широкую известность получила после выхода
    фильма Глеба Панфилова &quot;Прошу слова&quot; (1975), где звучит два раза. В первый раз песню поют хором старые большевики,
    поздравляя товарища с награждением орденом Ленина. Во-второй раз героиня под фонограмму Хора имени Пятницкого моет дома полы.<br>
