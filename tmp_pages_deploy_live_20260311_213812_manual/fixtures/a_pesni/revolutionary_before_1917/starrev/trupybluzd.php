<br>
За 19е июля 1906 г расстреляны 19 матросов на Косе. Память им стих в Кронштадте. <br><br>
<em>РГА ВМФ,  ф. 1340, оп. 1. д. 353. (Прислал капитан 2 ранга в отставке С.Ш. Бар 10 ноября 2016 г.)<br></em> 
  <br><br>
  Стихотворение В. Богораза-Тана, ставшее песней, о 19 матросах - участниках Кронштадтского восстания 
  19-20 июля 1906 г., приговоренных 17 сентября того же года к расстрелу и расстрелянных 21 сентября на батарее Литке. 
  Трупы уложили в мешки, погрузили на пароход и сбросили в Финский залив у Толбухина 
  маяка. Через несколько дней их прибило к берегу возле царского дворца в Петергофе. 
   Как песня известна также также под 
  заглавиями &quot;Царские гости&quot; и &quot;Парад мертвецов&quot;.<br>
  <br> 
  Приговор матросам соответствовал тяжести их деяний в юридических нормах того времени. Еще ранее - 20 июля, при подавлении восстания, у той же батареи расстреляли 7 нижних чинов минной роты за убийство своих командиров и женщины - близкой одного из них. <br>
  <br>
  Восстаниям на Балтийском флоте 17-20 июля 1906 года посвящены также песни <a href="mysamikopali.htm">&quot;Мы 
  сами копали могилу свою&quot;</a> того же В. Богораза-Тана, <a href="morevjar.htm">&quot;Море 
  в ярости стонало&quot;</a> Г. Ривкина и анонимная <a href="pobedazavami.htm">&quot;Победа 
  за вами осталась, враги&quot;</a>. <br>
  <br>
  <br>
  <strong>Восстание на Балтийском флоте 17-20 июля 1906 года</strong><br>
  <br>
  Восстание началось в ночь с 17 на 18 июля в морской крепости Свеаборг в Финляндии. 
  Через два дня (в ночь с 19 на 20 июля) восстал Кронштадт; над фортом &quot;Константин&quot; 
  - единственным укреплением, которое удалось взять под контроль - минеры подняли 
  красное знамя с надписью &quot;Земля и Воля&quot;. В ту же ночь в бухте Папон-вик 
  в 60 км от Ревеля восстал крейсер &quot;Память Азова&quot;. Все выступления 
  подавлены 20 июля. По советским данным, всего было расстреляно 28 человек в Свеаборге (17 матросов, 
  9 солдат, 2 офицера), 36 - в Кронштадте (19 матросов, 14 солдат, 3 штатских: 
  студент, рабочий и крестьянин), 18 - на крейсере &quot;Память Азова&quot;; 2310 
  человек приговорены к каторге, тюремному заключению и дисциплинарным батальонам.<br>
