Сколько целовал я истомленных уст, <br>
Но в жизни путь был всё же пуст. <br>
Сколько в жизни я провел ночей без сна, <br>
Сколько раз казалось, что пришла весна, <br>
Сколько раз любил напрасно, <br>
Но теперь мне стало ясно, <br>
Что с былым порвать пора. <br>
