До встречи с вами <br>
Не пел я этой песни. <br>
До встречи с вами <br>
Не знал я счастья вновь. <br>
Но с этой песней <br>
Вся жизнь мне <br>
Кажется чудесней. <br>
Я с этой песней <br>
Узнал свою любовь. <br>
