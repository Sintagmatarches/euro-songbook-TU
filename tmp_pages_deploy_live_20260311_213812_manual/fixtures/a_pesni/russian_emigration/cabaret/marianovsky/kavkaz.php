  &quot;Ты, душа лубэзный, савсэм мнэ под пару. <br>
  Ты цвэтёшь, как роза родного Кавказа. <br>
  Будэм ми с тобой жениться, радасть моя. <br>
  <br>
  &quot;Ах, оставь  ты, старый Карапэт, <br>
  У мине муж малодой Ахмет, <br>
