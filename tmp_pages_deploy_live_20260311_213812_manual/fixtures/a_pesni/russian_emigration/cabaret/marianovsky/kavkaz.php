  То тебе атрубит галава&quot;. <br>
  <br>
  ...На Кавказе есть гора самая болшая, <br>
  А внизу течет Кура, мутная такая, <br>
  Если на гор<strong>у</strong> залезть и с нее кидаться, <br>
  Очень много шансов есть <br>
  С жизнями расстаться... <br>
  <br>
  И весь народ кавказский <br>
  Любит любовь и ласки, <br>
  Если ж обманут глазки - <br>
  Вай! <br>
  Будем ми дурной хадить и тачить кинджалы, <br>
  А потом ее, х-ч, режим, чтоб не убежалы. <br>
  <br>
  Карапет приходит под вечер к Ахмеду:<br>
  &quot;Эй, Ахмет, ты хочишь большая монэту? <br>
  Мнэ отдай Тамару, канэчно, за это, <br>
  Будим ми на нэй жэниться, ехать в Тифлис&quot;. <br>
  <br>
  Говорит Ахмет: &quot;Зачем же нэт? <br>
  Женщин много, мало так монэт... <br>
  Забирай жену, запьём вином, <br>
  Потерал адну, так пять найдем! <br>
  <br>
