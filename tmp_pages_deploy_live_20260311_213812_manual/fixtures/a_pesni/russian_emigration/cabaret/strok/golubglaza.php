&quot;Голубые глаза&quot; делались на заказ - Строку их заказал один из поклонников 
  жены хозяина рижского кинотеатра &quot;Splendid Palast&quot; Емельянова, красавицы-эстонки 
  с голубыми глазами. Ноты вышли с посвящением: &quot;М. И. Ем-вой&quot;.<br>
  <br>
  Более известен текст этой мелодии на идише, сочиненный Игорем С. Корн-Тайером (Igor S. Korntayer, он же Igor S. Korn-Teuer, I. Korntayer, I. M. Korentajer, M. Korn-Teuer, S. Korn-Teuer, Samuel Korn-Teuer) для Менаше Оппенгейма - וווהין זאל א'ך ג''ן (&quot;Vuhin zol ikh gein?&quot; - &quot;Куда мне идти?&quot;). Корн-Тайер (1890-1941) работал актером и автором театров варьете сначала в Лодзи, затем в Варшаве. Попал в Варшавское гетто, погиб в Аушвице. Есть  <a href="http://russian-records.com/details.php?image_id=48610" target="_blank">запись на пластинку</a> Оппенгейма 1941 г. (Asch Recordings, New York, 1941, №H-6003 A). В 1950-е гг. песню пели сестры Берри, 
   в 1963 г. в английском переводе спел Рэй Чарльз (&quot;Where Can 
  I Go?&quot;). <br>
