<br>
<br>
Танго написано в 1931 году специально для Юрия Морфесси. <br>
<br>
<br>
Из книги Дмитрия Драгилёва &quot;Лабиринты русского танго&quot; (Спб., Алетейя, 
2008):<br>
<br>
&quot;В Берлине Строк услышал ансамбль «Weintraub Synkopatrs» с Эдди Рознером. 
Этот ансамбль исполнял тогда песню «Когда снова наступит весна и если ты снова 
будешь со мной...» Пауля Абрахама, композитора, чье творчество Строк оценивал 
высоко и чью судьбу впоследствии часто сравнивал со своей (премьера оперетты Абрахама 
«Бал в Савойе» состоялась в Берлине 1932 г., год спустя композитор вынужден был 
бежать в Вену). Песенка веселая, мажорная. Строк написал грустное танго, припев 
которого начинается с мелодического оборота, ритмически и интонационно родственного 
началу припева танго «Черные глаза». Написал его специально для исполнителя русских 
романсов Юрия Спиридоновича Морфесси, которого композитор знал еще по Петербургу. 
В Риге Морфесси сильно увлекся сестрой приятеля Строка, а в Париже у певца была 
серьезная любовная драма, связанная с неудачной женитьбой на В.В. Лозовской. История 
получила огласку в среде русской эмиграции. Покинув Париж, Морфесси предполагал 
поселиться в Риге, однако уехал жить в Югославию. Между прочим, события вокруг 
Лозовской были также связаны не только с Парижем, но и Югославией. В этом контексте, 
видимо, следует воспринимать использованную в первой аранжировке танго цитату-реминисценцию 
романса «Не для меня придет весна, не для меня Дунай разольется». Сам по себе 
прием романсового цитирования был весьма распространен в ту пору. Русскому слушателю 
он напоминал о музыкальной преемственности и корнях русского танго, а иностранцу 
помогал острее почувствовать русский стиль (таковы цитата из романса «Очи черные» 
в аранжировке танго «Черные глаза», цитата из романса «Не уходи, побудь со мною» 
в танго «Весна» Вадима Козина). Парижская тема «возвращала» Строка к сюжету с 
Лени Либман, отсюда, возможно, сходство ритмического и мелодического развития 
начальных тактов припева двух танго: фактически начало припева танго «Когда весна 
придет опять» представляет из себя модифицированное начало припева танго «Черные 
глаза»&quot;. <br>
