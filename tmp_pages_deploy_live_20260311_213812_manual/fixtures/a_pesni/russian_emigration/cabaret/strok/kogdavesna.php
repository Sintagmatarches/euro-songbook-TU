Слова и музыка Оскара Строка<br>
<br>
Мы с тобою жили так беспечно,<br>
Но счастье хрупко, невечно.<br>
Ты мне сказала: Прощай.<br>
Жди, весною я буду снова с тобою…<br>
Я тебя не смог удержать,<br>
Одиноким остался опять.<br>
<br>
<em>Припев:</em><br>
<br>
Когда весна опять придет<br>
И солнце ярко засияет,<br>
Печаль о прошлом отойдет<br>
И будешь вновь моей,<br>
И будет нежно смех звучать,<br>
Как колокольчик утром мая,<br>
И я приду тебя обнять,<br>
Любимая, родная.<br>
<br>
Дни тянутся длинной чередою,<br>
Лето сменилось зимою,<br>
Снегом покрылись поля.<br>
И, тоскуя, ждал тебя одну я.<br>
Снились мне печальные сны,<br>
Что не будет тебя и весны.<br>
<br>
<em>Припев.</em><br>
