<br>
Когда весна придет опять</strong><br>
<br>
Слова и музыка Оскара Строка<br>
<br>
Мы с тобою жили так беспечно,<br>
Но счастья милость не вечна,<br>
Ты мне сказала &quot;прощай&quot;,<br>
Жди весною, буду я вновь с тобою.<br>
Я не смог тебя удержать,<br>
Одиноким остался опять.<br>
<br>
<em>Припев:</em><br>
<br>
Когда весна придет опять<br>
И солнце ярко засияет,<br>
Печалей прошлого печать<br>
Растопит светлый миг.<br>
И станет снова смех звучать,<br>
Как колокольчик утром мая.<br>
Чтобы влюбленных повенчать,<br>
Весна приходит в мир.<br>
<br>
Дни тянулись длинной чередою.<br>
Осень сменилась зимою,<br>
Снегом укрылись поля.<br>
Мгла скрывает<br>
Образ твой, но снова я<br>
Слышу как мотив, как предлог<br>
Для того, чтобы их унесло.<br>
<br>
<em>Припев:</em><br>
<br>
Когда весна придет опять?<br>
Нет смысла ждать тебя украдкой.<br>
Не повернуть нам время вспять<br>
От шуток и вранья.<br>
Так станет снова чья-то быль<br>
Рассказом сдержанным и кратким<br>
Для тех, кто в мире может быть<br>
Доверчивей, чем я.<br>
<br>
<em>Драгилёв Д. Лабиринты русского танго. СПб., Алетейя, 2008.</em><br>
