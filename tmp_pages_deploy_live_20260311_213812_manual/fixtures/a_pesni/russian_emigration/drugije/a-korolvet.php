<span class='bold'>Людмила 
  Клигман<br></span>
  <h1>КОРОЛЬ «ВЕЧНОЗЕЛЕНЫХ» ТАНГО</h1>
  <span class='i'><a href="http://www.migdal.ru/times/73/" target="_blank">Мигдаль Times</a>. 2006. №73-74. Июль <br></span>
  <br>
  <br>
«Утомленное солнце нежно с морем прощалось...» Самое популярное танго 
  30-х годов прошлого века. Бархатный голос Павла Михайлова, популярного исполнителя 
  «Утомленного солнца», звучит в фильме Н. Михалкова с похожим названием. Правда, 
  события фильма происходят в 1936-м году, а Михайлов записал эту песню на пластинку 
  в 1937-м, да и текст отличался от звучащего в фильме. А родилось это танго в 
  Польше — естественно, со словами на польском языке...<br>
  <br>
  Его автор — уроженец Варшавы Ежи Петерсбурский, некогда знаменитый, а ныне подзабытый 
  композитор, пианист и дирижер, которого в польских энциклопедиях называют «настоящей 
  легендой “легкой” музыки».<br>
  <br>
  Он появился на свет в 1897 г. в знаменитой клезмерской семье с музыкальной фамилией 
  Мелодиста. (Впоследствии за границей Ежи Мелодиста стал известен как Ежи (Георгий, 
  Юрий) Петерсбурский, а также под псевдонимом Уоренс.) Отец, Якоб Мелодиста, 
  был певцом, мать Паулина — пианисткой. Она и научила Ежи игре на фортепиано, 
  и, по воспоминаниям, он превосходно играл с четырех лет. Музыкантами были и 
  братья: Йозеф — скрипачом, Станислав — пианистом, а кузен Артур Голд — композитором.<br>
  <br>
  Ежи окончил Варшавскую консерваторию в 1916 году и продолжил обучение у профессора 
  Артура Шнабла в Вене, где совершенствовался как дирижер. Будущий король танго 
  готовился посвятить себя серьезной музыке, но встреча с великим Имре Кальманом 
  изменила его творческие планы. Автор «Королевы чардаша» и «Сильвы» убедил юного 
  композитора бросить серьезную музыку и сочинять популярные песни. И Кальман 
  не ошибся: большинство изящных, элегантных танго, написанных Ежи, стали «evergreen» 
  — «вечнозелеными», не утратившими популярности за многие десятилетия. И ныне 
  не покидают эстрады самых престижных концертных залов танго «О, донна Клара», 
  «Уж никогда», «Маленькое счастье», «Ты сам мне говорил», «Ты и эта гитара»...<br>
  <br>
  Именно танго — повальное увлечение всего мира в период между двумя мировыми 
  войнами — принесло Петерсбурскому мировую славу. А еще были инструментальные 
  танцевальные пьесы, вальсы и фокстроты, оперетты, музыка к заговорившему в те 
  годы «великому немому» (комедии «Что мой муж делает по ночам?», «Робер и Бертран»), 
  работа с Александром Вертинским...<br>
  <br>
  <img src="a-korolvet1.jpg" alt="Оркестр Артура Голда и Ежи Петерсбурского"><br>
  <span class='i'>Оркестр Петерсбурского</span><br>
  <br>
  Вернувшись в Польшу, Петерсбурский вместе с Артуром Голдом в 1926 году основал 
  оркестр, быстро ставший знаменитым. Сам Ежи играл на фортепьяно, а Голд — на 
  скрипке. На рубеже 1920-30-х гг. это был, пожалуй, популярнейший танцевальный 
  оркестр в Варшаве, он выступал в самом фешенебельном ресторане «Aдрия». Слушатели 
  часто повторяли слова одной из песен Ежи: «Когда Петерсбурский играет с Голдом, 
  вы не будете спать всю ночь до рассвета». Оркестр давал концерты в «Адрии» в 
  течение 8 лет — дольше, чем любые другие артисты.<br>
  <br>
  Кроме игры в кабаре и ночных клубах, коллектив записывал много пластинок с польскими 
  «звездами» — группой «Chor Dana», Мечиславом Фоггом, Эугениушем Бодо, Людвиком 
  Семполински, Верой Бобровской, Ганкой Ордон. Позже Голд ушел из оркестра, предпочтя 
  сольную карьеру. А его кузен продолжал выступать — теперь уже просто с «Оркестром 
  Ежи Петерсбурского». К его мелодиям писали лирику Анджей Власт, Людвик Смарагд, 
  Мариан Хемар, Артур Тэр.<br>
  <br>
  В 30-е годы музыкальная жизнь в Польше была яркой и бурной. Кабаре и варьете, 
  театры миниатюр и оперетты, музыкальные салоны и концертные залы никогда не 
  пустовали. И одним из ведущих композиторов того времени был Ежи Петерсбурский.<br>
  <br>
  Интересна история «жемчужины кабаре» — песни «О, донна Клара» (оригинальное 
  польское название — «Танго Милонга»), написанной Петерсбурским в соавторстве 
  с поэтом А. Властом. Песня в исполнении «королевы танго» Станиславы Новицкой 
  стала хитом. А когда оркестр Петерсбурского выступал в Вене, влиятельный венский 
  музыкальный издатель Вайнер Бохем предложил Ежи 3000 шиллингов за право записать 
  «Танго Милонга» — но с условием: изменить название. Автор согласился, и так 
  родилась «О, донна Клара» — с немецким текстом Фрица Ленер-Беда. Песня была 
  переведена на несколько языков, появились оригинальные немецкие и английские 
  тексты. «О, донна Клара» стала очень популярной в Европе и Америке. Ее пели 
  «звезды» — Пола Негри, Ал Джолсон, Генри Варни. В Польше «О, донна Клара» признана 
  лучшим отечественным шлягером 20 века.<br>
  <br>
  В 1930-32 гг. Ежи Петерсбурский выступал в Вене, Давосе, Берлине, Праге. В Париже 
  он написал шлягер «Ни я, ни ты», исполненный Эдит Пиаф. Как первый польский 
  композитор, чьи песни стали настолько популярными за рубежом, Петерсбурский 
  в 1936 году получил награду от польского президента Игнация Мосцицкого.<br>
  <br>
  <img src="a-korolvet3.jpg" alt="Мечислав Фогг"><br>
  <span class='i'>Мечислав Фогг</span><br>
  <br>
  Тогда же появилось самое знаменитое танго Ежи Петерсбурского. Варшавский поэт-песенник 
  Зенон Фридвальд предложил композитору текст песни под названием «Та остатня 
  недзеля» («Последнее воскресенье») — о расставании бывших влюбленных. Молодой 
  человек просит возлюбленную встретиться с ним в воскресенье в последний раз: 
  девушка уходит от него к более состоятельному жениху. Петерсбурского привлекли 
  лиризм и грусть, сквозившие в каждой строке. Написав к этому тексту танго, Ежи 
  отдал его известному певцу Мечиславу Фоггу, долгожителю польской эстрады. Успех 
  первого исполнения превзошел все ожидания. Предприимчивые владельцы фирмы звукозаписи 
  «Сирена Электро» предложили Фоггу записать его новую работу на пластинку. Многотысячный 
  тираж разошелся очень быстро, его пришлось повторить. Но и на этот раз спрос 
  на пластинку не был удовлетворен. Мелодия нового танго Петерсбурского в исполнении 
  Фогга зазвучала по всей Польше, а затем вышла за ее пределы. Танго звучало в 
  танцевальных салонах и концертных залах, везде, где имелись патефоны. В 1937 
  году танго Ежи Петерсбурского долетело и до Советского Союза и завоевало огромную 
  популярность — уже с русским, совершенно новым текстом поэта Иосифа Альвека, 
  под названием «Утомленное солнце». В замечательной аранжировке Александра Цфасмана 
  танго было записано на пластинку и получило название «Расставание». Увы, автор 
  музыки был «позабыт», и песня оказалась как бы ничейной. Пластинка с танго «Расставание» 
  сразу стала предметом повышенного спроса. Апрелевский и Ногинский заводы Главпласттреста 
  с трудом удовлетворяли его. «Расставание» стало обязательным атрибутом семейных 
  и выпускных вечеров, дворовых концертов, всяческих застолий. Многие эстрадные 
  исполнители включали его в свой репертуар, зачастую с различными вариантами 
  слов. Например, в Ленинграде особой популярностью пользовался текст местной 
  поэтессы Асты Галлы под названием «Песня о юге» (в исполнении Клавдии Шульженко). 
  А квартет Александра Рязанова пел на ту же мелодию «Листья падают с клена». 
  Через много лет это танго появилось во множестве фильмов, включая «Сказку сказок» 
  Юрия Норштейна (1979), признанную международными критиками лучшим анимационным 
  фильмом всех времен.<br>
  <br>
  <img src="a-korolvet2.jpg"><br>
  <br>
  К сожалению, кроме триумфа, в истории «Последнего воскресенья» есть очень грустные 
  страницы. Его называли и «Танго Смерти»: в нацистских концлагерях под звуки 
  этой мелодии, исполняемой маленьким духовым оркестром, еврейских заключенных 
  вели в газовые камеры. В Треблинке Артур Голд вынужден был руководить таким 
  оркестром и хором до самой своей гибели*. После Победы песня зазвучала в польских 
  кинофильмах о зверствах фашистов.<br>
  <br>
  Ее автору повезло больше, чем Голду. В 1939 году Петерсбурский служил сержантом 
  в польских Воздушных силах. После раздела Польши Ежи, как и многие польские 
  евреи, оказался в Советском Союзе. Он руководил Белорусским республиканским 
  джаз-оркестром, собиравшим переполненные залы. Позже Ежи снова организовал «Оркестр 
  Петерсбурского и Голда» — на сей раз вместе с братом Артура Голда Хенриком.<br>
  <br>
  <img src="a-korolvet4.jpg" alt="Клавдия Шульженко"><br>
  <span class='i'>Клавдия Шульженко</span><br>
  <br>
  В СССР Петерсбурский написал одну из самых прекрасных военных песен — «Синий 
  платочек», прославивший Клавдию Шульженко. Есть несколько вариантов «Платочка». 
  Первый, довоенный, прозвучал в 1940 году в московском театре «Эрмитаж» на гастролях 
  оркестра Петерсбурского. Потом песню записали на пластинки Изабелла Юрьева, 
  Екатерина Юровская, Лидия Русланова (пластинка Руслановой в тираж не вышла, 
  но запись сохранилась). Все эти записи — с указанием автором текста Якова Галицкого. 
  «Военный» вариант текста появился в 1942 году на Волховском фронте, когда лейтенант 
  Михаил Максимов передал Шульженко, гастролировавшей с джазом Алексея Семенова, 
  листок со стихами о «синеньком скромном платочке». Она спела его в тот же день, 
  после единственной репетиции...<br>
  <br>
  Так появилась первая военная лирическая песня, которая распространилась с быстротой 
  ветра. Ее немудреный текст бойцы записывали под диктовку Шульженко, приезжавшей 
  с концертами на передовую, узнавали от друзей-товарищей и посылали в своих письмах 
  с фронта в тыл (аналогичная ситуация повторится с «Жди меня» К. Симонова). Рассказывали, 
  что когда наши бойцы на передовой громко включали «Синий платочек», немцы прекращали 
  стрелять. По сообщению московского радиоканала «Содружество», однажды был взят 
  в плен немецкий часовой, заслушавшийся «Платочком»...<br>
  <br>
  В советскую фронтовую песню «Огонек» превратился еще один хит Ежи Петерсбурского 
  — «Польское танго», в русской версии получившее название «Стелла». Чрезвычайно 
  популярен в СССР был и его фокстрот «Май, цветущий май».<br>
  <br>
  Как дальше сложилась судьба композитора? После заключения польско-советского 
  пакта Сикорского-Майского в 1941 году Петерсбурский присоединился к Польской 
  армии генерала Владислава Андерса, которая отправилась воевать в Северной Африке. 
  Таким образом, Петерсбурский в 1942-1943 годах оказался в Каире, где работал 
  на польском радио.<br>
  <br>
  После войны он много путешествовал, побывал в Палестине, потом обосновался в 
  Бразилии, где сначала работал в фортепианном дуэте с другом из довоенной Польши 
  Альфредом Шуцем.<br>
  <br>
  В 1948-68 годах Ежи Петерсбурский жил в Аргентине и работал на «Радио эль Мондо» 
  в Буэнос-Айресе. В 1952 году была написана песня-хит «Все дороги ведут в Буэнос-Айрес», 
  до сих пор оставшаяся в радиопозывных. В Аргентине Петерсбурский совместно с 
  еще одним польским евреем, актером кабаре Казимижем Круковски («Лопеком»), в 
  течение почти 20 лет руководил оркестром Национального театра. Поставил мюзикл 
  «Blue Sky Milonga», сотрудничал со знаменитым композитором Астором Пьяцолла. 
  После смерти жены, Марии Минковски, во время землетрясения в 1967 году, Петербургский 
  переехал в Венесуэлу, а в 1968-м вернулся в Польшу. В Варшаве он снова женился 
  — на оперной певице Сильвии Клеждич. В 1969 году у них родился сын — Ежи Петерсбурский-младший 
  (сегодня это известный польский актер, музыкант, композитор, телеведущий).<br>
  <br>
  Ежи Петерсбурский умер на родине в 1979 году.<br>
  <br>
  В самой Польше отношение к этому композитору в разные времена было неоднозначным. 
  В период расцвета «Солидарности» была запущена «английская» версия происхождения 
  «Синего платочка». Кое-кому очень не хотелось, чтобы автором песни-символа Советской 
  Армии был поляк (да еще написавший песню на советской территории). Дело доходило 
  до того, что «Последнее воскресенье» в Польше в 80-90-х выпускали на дисках 
  без указания автора музыки.<br>
  <br>
  Сегодня Петерсбурский снова популярен на родине. Его изысканные танго опять 
  звучат — теперь уже в исполнении известного артиста Петра Фрончевски.<br>
  <br>
