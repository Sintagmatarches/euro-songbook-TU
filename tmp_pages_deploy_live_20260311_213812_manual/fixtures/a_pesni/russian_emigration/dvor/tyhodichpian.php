  <strong> 2. Ты ходишь пьяная и очень бледная...</strong><br>
  <br>
  Ты ходишь пьяная и очень бледная<br>
  По темным улицам совсем одна.<br>
  Тебе мерещится дощечка медная<br>
  И штора синяя его окна.<br>
  <br>
  Прижавшись к бархату авто бесшумного,<br>
  Ты грустно смотришь в ряды огней.<br>
  И память прошлого, полубезумного,<br>
  Мелькают ужасы былых ночей.<br>
  <br>
  А на диване там подушки алые,<br>
  Вино шампанское, коньяк «Мартель».<br>
  Глаза янтарные, всегда печальные,<br>
  Губы искусаны, сама пьяна.<br>
  <br>
  Супруг, обманутый тобой, неверною,<br>
  Придя в гостиную, супругу ждет.<br>
  Любовник знает: она покорная,<br>
  Кляня и плача, к нему придет.<br>
  <br>
  Ты входишь пьяная, полувеселая,<br>
  Маня рукой его, к себе прижмешь.<br>
  Лишь только лампочки с рассветом тусклые,<br>
  А ты усталая домой бредешь.<br>
  <br>
  Ты ходишь пьяная, такая странная,<br>
  По темным улицам совсем одна.<br>
  Тебе мерещится вся жизнь пропащая<br>
  И штора синяя его окна.<br>
  <br>
  <em>В нашу гавань заходили корабли. Пермь, &quot;Книга&quot;, 1996. </em><br>
