  <br>
  <em>Расшифровка фонограммы Николая Маркова &quot;на ребрах&quot;, 
  ок. 1953-54 гг. Приводится по статье В. Солдатова <a href="#1">&quot;Журавли в исполнении Николая Маркова 
  и «Джаза Табачников»&quot;</a>).</em><br>
<br>
  <br>
  Песня на основе стихотворения Алексея Жемчужникова &quot;Осенние журавли&quot;, 
  написанного в 1871 году. Появилась не позднее середины 
  1930-х годов (ее запись на пластинку Н. Шевцовым датируется временем не позднее 
  1935 года).<br>
  <br>
  В начале 1950-х годов (после смерти Сталина?) получила широкое хождение в СССР на 
  &quot;ребрах&quot; - кустарных пластинках из использованной рентгеновской фотопленки. 
  Делали их подпольно, продавали на толкучках - это был &quot;народный ответ&quot; 
  на борьбу с формализмом: в те годы романсы, джаз, фокстроты и т. п. были под 
  официальным запретом и на государственных пластинках не выходили. &quot;Ребра&quot; 
  выдерживали максимум 20-30 воспроизведений. &quot;Журавлей&quot; для ребер исполнил 
  Николай Марков, солист бывшего &quot;Джаза табачников&quot;, и пластинка продавалась 
  как фонограмма Петра Лещенко. Всего Марков записал на ребрах 40 песен &quot;из 
  репертуара Лещенко&quot;. Причем, настоящий Петр Лещенко (1898-1954) никогда 
  не записывал на пластинку &quot;Журавлей&quot; (его полную дискографию см.: 
  Очи черные: Старинный русский романс. М.: Изд-во Эксмо, 2004, стр. 245-250) 
  и никогда их не исполнял.<br>
  <br>
  В интернете песня встречается с авторством Лещенко, иногда даже с указанием 
  на авторство музыки работавших с Лещенко &quot;короля танго&quot; Оскара Строка 
  или дирижера Жоржа Ипсиланти, но эти версии авторства опровергнуты.<br>
  <br>
  В СССР возникли &quot;лагерные&quot; варианты песни, в том числе <strong>&quot;Журавли 
  над Колымой&quot; </strong> (см. также близкую песню <a href="kolyma.php">&quot;Колыма&quot;</a>). 
  Есть и более поздние переделки, например, <a href="../afgan/zurafgana.php">&quot;Журавли 
  Афгана&quot;</a>.<br>
  <br>
