  С песней «Журавли» в сердце и памяти я живу уже почти полвека. Впервые услышал 
  её в 1955 году с грампластинки «на рёбрах». Хорошо помню, как сочувственно мы, 
  молодые ребята, воспринимали тогда ностальгию по Родине, аж слеза прошибала… 
  Причём ностальгия была глубинная – по России, которая существовала в нашем мироощущении 
  только как старое название СССР. Мы ведь тоже были оторваны от родительских 
  гнёзд, жили в общежитии техникума, скучали по родственникам и друзьям. На грампластинке 
  от руки было написано «Лещенко». О нём ходили тогда разные слухи – будто бы 
  и на немцев работал (пел для них в ресторане) и тайно подпольщикам Одессы помогал. 
  Внук и его друзья помногу раз подряд слушают теперь запись М. Круга «Кольщик», 
  а для нас таким же фантомом была песня «Журавли».<br>
  <br>
  Время было переломное и тревожное. В марте 1953-го умер товарищ Сталин. Когда 
  утром по станичному радио объявили о трёхдневном трауре и что школы работать 
  не будут, я подпрыгнул от радости на кровати и закричал «Ура!». Отец с матерью 
  зашикали на меня, что-то объясняли. Взрослые везде были нахмуренными, а кое-кто, 
  в том числе мои родители, плакали. И так продолжалось много дней. Я понял, что 
  произошло очень серьёзное событие. Уходило прошлое, а будущее вызывало тревогу 
  – тревогу о завтрашнем дне. Как-то оно всё сложится?.. В том же году, идя из 
  школы, я увидел на улице растоптанный фотопортрет мужчины в рамочке с разбитым 
  вдребезги стекольцем. Неподалёку ветер разносил обрывки полусгоревших бумажек 
  с краями портрета. А один портрет, как бумажка из уборной, был смят и испачкан 
  дерьмом. Дома я спросил у отца с матерью, что это за портрет был, кто на нём 
  сфотографирован? Ответили: «Лаврентий Берия». «А что, он плохой был?» - «Страшный. 
  Ты ни с кем об этом не говори…». <br>
  <br>
  ……Я не сразу просёк, что пластинка с песней «Журавли» была подпольная. Её и 
  другие такие же, с рёбрами на просвет, после проигрывания прятали. А она звучала 
  и звучит в моей памяти и без патефона (обратите внимание на то, в каких местах 
  «протягивает» исполнитель).<br>
