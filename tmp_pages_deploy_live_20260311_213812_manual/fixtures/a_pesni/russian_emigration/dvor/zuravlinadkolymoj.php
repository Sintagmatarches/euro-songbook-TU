  Вплоть до 2001 г. я был уверен, что песню «Журавли» записал Пётр Лещенко, как 
  и было написано на рентгенпластинке. Однако из книги Б.А. Савченко. Эстрада 
  ретро. - М.: Искусство, 1996, стр. 220 узнал, что это не так. Цитирую: «Оказалось, 
  в послевоенные годы в Москве на волне популярности Петра Лещенко успешно процветала 
  целая подпольная фирма по выпуску и распространению пластинок «под Лещенко». 
  Костяк фирмы составили так называемый «Джаз табачников» (там одно время работал 
  и композитор Борис Фомин) и его солист Николай Марков, голос которого был почти 
  идентичен голосу знаменитого певца. За короткое время было записано сорок произведений 
  из репертуара Лещенко, в том числе и не имевшие к нему отношения «Журавли». 
  Пластинки распространялись в основном на Украине, в Молдавии… Один музыкант 
  из «Джаза табачников» говорил по этому поводу так: «Туда везём чемодан пластинок, 
  обратно – чемодан денег»… Возникала полная неразбериха: официально пластинки 
  Петра Константиновича Лещенко в магазинах не продавались, потому что и не выпускались, 
  а голос певца звучал почти в каждом доме. Подлинник или подделка – поди догадайся».<br>
  <br>
  В результате завязавшейся переписки В. Сысоев выслал мне запись песни «Журавли» 
  в формате МР-3. Своё письмо он закончил вопросом: «Лещенко, действительно, если 
  верить отечественным коллекционерам, никогда не пел &quot;Журавли&quot;. И Вертинский 
  не пел. Тогда просто непонятно, откуда в сталинские годы у этого танго была 
  такая популярность? Неужели только от двух записей на &quot;ребрах&quot;? Вопрос 
  резонный. Я думаю, что запись Н. Марковым песни «Журавли» в числе сорока других 
  из репертуара П. Лещенко, свидетельствует о том, что музыканты «Джаза табачников», 
  как и неизвестные советские офицеры, освобождавшие Восточную Европу, имели либо 
  ту грампластинку 40-х годов с его записями, либо ту ресторанно-концертную запись 
  «У Максима 37», о которых рассказал А.Г. Яцковский в интервью «Местная шпана 
  меня избегала…». По сравнению с текстом, который приводит Яцковский, песня элегантно 
  правлена с учётом того, что её будут воспринимать здесь, а не там, и спета без 
  повтора строк, по-видимому, для сокращения времени звучания. Вспоминаю, что 
  в «Ночном такси» Алла Баянова говорила А.Фрумину, что она, когда пела «Журавлей», 
