  “Голоса Крыма” находилась буквально в 100 метрах от театра. Сама же газета педантично 
  отслеживала на своих страницах все публичные театрально-эстрадные и другие культурные 
  мероприятия, проходившие в Симферополе. Подчеркиваю, все публичные мероприятия, 
  даже маломальские по значению своему. Например, выступление духового оркестра 
  местной полиции.<br>
