Слова и музыка Александра Вертинского<br>
<br>
Я знаю, Джимми, вы б хотели быть пиратом.<br>
Но в наше время это невозможно.<br>
Вам хочется командовать фрегатом,<br>
Носить ботфорты, плащ, кольцо с агатом,<br>
Вам жизни хочется опасной и тревожной.<br>
<br>
Вам хочется бродить по океанам<br>
И грабить шхуны, бриги и фелуки,<br>
Подставить грудь ветрам и ураганам,<br>
Стать знаменитым «черным капитаном»<br>
И на борту стоять, скрестивши гордо руки…<br>
<br>
Но, к сожалению, вы мальчик при буфете<br>
На мирном пароходе – «Гватемале»,<br>
На триста лет мы с вами опоздали,<br>
И сказок больше нет на этом скучном свете.<br>
<br>
Вас обижает метр за допитый коктейль,<br>
Бьет повар за пропавшие бисквиты.<br>
Что эти мелочи – когда мечты разбиты,<br>
Когда в двенадцать лет уже в глазах печаль!<br>
<br>
Я знаю, Джимми, если б были вы пиратом,<br>
Вы б их повесили однажды на рассвете<br>
На первой мачте вашего фрегата…<br>
Но вот звонок, и вас зовут куда-то…<br>
Прощайте, Джимми, сказок нет на свете!<br>
