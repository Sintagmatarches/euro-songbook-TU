Что прислал золотых парадизов.<br>
Только он в санаторьях Каира<br>
Умирает от ваших капризов!<br>
<br>
4. И мне жаль, что на тысячи метров<br>
И любви, и восторгов, и страсти<br>
Не найдется у вас сантиметра<br>
Настоящего личного счастья.<br>
<br>
Но сегодня весна беспечальна,<br>
Как и все ваши кинокапризы.<br>
