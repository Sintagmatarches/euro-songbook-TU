 <br>
  <strong>Михаил Волин (Михаил Николаевич Володченко, 1914-1997)</strong> - поэт и прозаик. Родился на станции Имяньпо (КВЖД). Дед поэта Николай Герасимович Володченко (1862—1945) - генерал-лейтенант, участник русско-японской и Первой мировой войн, в 1905-1914 начальник штаба Заамурского военного округа, в 1917 году командовал  Юго-Западным фронтом. Отец - секретарь посольства Российской Империи в Монголии. В Китай родители поэта переселились в конце ХIХ века.
Стихи начал писать в школе (Харбинской гимназии Христианского союза молодых людей). Работал корреспондентом.
В 1937 переехал из Харбина в Шанхай, где открыл школу гимнастики и йоги. В 1949  эмигрировал в Австралию, жил в Сиднее. В 1969-1981 жил в США, затем вернулся в Австралию и поселился в Аделаиде, где и умер. 
Помимо стихотворений и художественной прозы автор более десятка книг по хатха-йоге. <br>
