	<span class="header"><strong>ЖЕЛТЫЙ АНГЕЛ </strong><br></span>
  <br>
  Слова и музыка Александра Вертинского<br>
  <br>
  В вечерних ресторанах,<br>
  В парижских балаганах,<br>
  В дешевом электрическом раю<br>
  Всю ночь ломаю руки<br>
  От ярости и скуки<br>
  И людям что-то жалобно пою.<br>
  <br>
  Звенят, гудят джаз-баны,<br>
  И злые обезьяны<br>
  Мне скалят искалеченные рты.<br>
  А я, кривой и пьяный,<br>
  Зову их в океаны<br>
  И сыплю им в шампанское цветы.<br>
  <br>
  А когда настанет утро, я бреду бульваром сонным,<br>
  Где в испуге даже дети убегают от меня.<br>
  Я усталый старый клоун, я машу мечом картонным,<br>
  И в лучах моей короны умирает светоч дня.<br>
  <br>
  Звенят, гудят джаз-баны,<br>
  Танцуют обезьяны<br>
  И бешено встречают Рождество.<br>
  А я, кривой и пьяный,<br>
  Заснул у фортепьяно<br>
  Под этот дикий гул и торжество.<br>
  <br>
  На башне бьют куранты,<br>
  Уходят музыканты,<br>
  И елка догорела до конца.<br>
  Лакеи тушат свечи,<br>
  Давно замолкли речи,<br>
  И я уж не могу поднять лица.<br>
  <br>
  И тогда с потухшей елки тихо спрыгнул желтый Ангел<br>
  И сказал: «Маэстро бедный, вы устали, вы больны.<br>
  Говорят, что вы в притонах по ночам поете танго,<br>
  Даже в нашем добром небе были все удивлены».<br>
  <br>
  И, закрыв лицо руками, я внимал жестокой речи,<br>
  Утирая фраком слезы, слезы боли и стыда.<br>
  А высоко в синем небе догорали божьи свечи,<br>
  И печальный желтый Ангел тихо таял без следа.<br>
  <br>
  1934<br>
