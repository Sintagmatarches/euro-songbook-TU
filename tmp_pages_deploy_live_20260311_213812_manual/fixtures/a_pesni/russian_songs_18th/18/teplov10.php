	1. К тому ли я тобой, к тому ли я пленялась,<br>
Чтоб, пламенно любя, всечасно воздыхать?<br>
На то-ль моя душа любовью загоралась,<br>
Чтоб мне потоки слез горчайших проливать?<br>
губить младые лета,<br>
Бесплодну страть питать,<br>
И все утехи света<br>
В тебе лишь почитать?<br>
<br>
2. В тебе! А ты меня безжалостно терзаешь,<br>
И сердце ты и дух в отчаянье привел.<br>
Иль ты еще моей горячности не знаешь?<br>
Приметь, мучитель, как ты мною овладел.<br>
Что в сердце ощущаю, -<br>
Пойми из слез моих:<br>
Как я тобой страдаю,<br>
Написано на них.<br>
