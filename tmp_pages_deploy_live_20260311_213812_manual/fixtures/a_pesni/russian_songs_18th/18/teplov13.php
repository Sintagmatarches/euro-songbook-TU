	<h1>МЫ ДРУГ ДРУГА ЛЮБИМ</h1>
	<br>
	Музыка Григория Теплова<br>
	Слова Александра Сумарокова<br>
	<br>
	1. Мы друг друга любим, что ж нам в том с тобою!<br>
Любим и страдаем всякой час.<br>
Боремся напрасно мы с своей судьбою,<br>
Нет на свете радостей для нас.<br>
С лестно надеждой наш покой сокрылся,<br>
Мысли безмятежные отняв.<br>
От сердец разженных случай удалился,<br>
Удалилось время всех забав.<br>
<br>
2. Зрю ль тебя, не зрю ли, равно грусть имею,<br>
Равное мучение терплю;<br>
Уж казать и взором я тебе не смею,<br>
Ах! ни воздыханьем, как люблю.<br>
Все любовные знаки в сердце заключенны,<br>
Должно хлад являли и гореть.<br>
Мы с тобой, драгая, вечно разлученны,<br>
Мне тебя осталось только зреть.<br>
<br>
3. Жизнь мою притяну пременил рок в злую,<br>
Сладость обращенна в горесть мне;<br>
Только ныне в мыслях я тебя целую,<br>
Лишь с тобою говорю во сне.<br>
Где любови нашей прежние успехи,<br>
Где они девалися, мой свет!<br>
О печально сердце, где твои успехи!<br>
Все прошло, и уж надежды нет!<br>
