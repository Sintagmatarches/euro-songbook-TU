	<h1>ГДЕ МНЕ УКРЫТЬСЯ, ЗЛОБНАЯ СУДЬБИНА</h1>
		<br>
	Музыка Григория Теплова<br>
	<br>
	1. Где мне укрыться, злобная судьбина,<br>
Где мне удобней воздыхать?<br>
Рощи прекрасны, тихая долина,<br>
Вам злу тоску хочу вещать.<br>
<div class="refren">Тайну сердца сокровенну<br>
Вам одним теперь явлю:<br>
Я питаю страсть смертельну<br>
И сердечно я люблю.</div>
<br>
2. Есть ли кто в свете, кто свою злу долю<br>
С долей моей бы смог сравнять?<br>
Бедный невольник,чувствуя неволю,<br>
Чтится отраду в том сыскать,<br>
<div class="refren">Что мучителю всечасно<br>
Может цепь свою казать,<br>
И его, хотя напрасно,<br>
Чтится оным умягчать.</div>
<br>
3. Я ж той лютой частью и того лиш<strong>е</strong>нна,<br>
Тщетно я трачу тяжкий стон:<br>
Чь<strong>и</strong> я носить цепи осуждена,<br>
Власти своей не знает он.<br>
<div class="refren">Что я вечно им пленилась,<br>
Стыдно то ему казать,<br>
Ах! к чему я в свет род<strong>и</strong>лась,<br>
Коль до смерти в нем рыдать.</div>
