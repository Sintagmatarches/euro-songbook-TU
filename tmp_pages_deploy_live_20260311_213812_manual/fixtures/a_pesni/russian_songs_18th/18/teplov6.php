	<h1>СКОЛЬКО ГРУСТИ И МУЧЕНЬЯ</h1>
	<br>
	Музыка Григория Теплова<br>
	<br>
	1. Сколько грусти и мученья<br>
Нам бесплодна страсть сулит!<br>
Сколько бегства и напастей<br>
В сей любви нам предстоит?<br>
Коль судьбина не согласна<br>
С нежной волею сердец,<br>
На какой, мой свет, ты страстна,<br>
На какой, увы, конец?<br>
<br>
2. Что в том пользы, дорогая,<br>
Хоть равна у нас любовь,<br>
Хоть однаким напоена<br>
Чувством наша жарка кровь?<br>
Тщетно сходны наши мысли,<br>
Рок противится любви.<br>
Ты своим меня не числи<br>
И оковы разорви.<br>
<br>
3. Возвращай свою свободу,<br>
пусть один лишь я грущу.<br>
Хоть останусь в злой печали,<br>
Но тебя не возмущу.<br>
Удалюсь очей любезных,<br>
В сих не буду жить местах.<br>
Ах, не трать ты в бесполезных<br>
Время вздохах и слезах!<br>
<br>
4. Позабудь меня, драгая,<br>
Позабудь меня, мой свет!<br>
Так судьбой определено, <br>
Нам в любови счастья нет.<br>
Мне рыдать повелевает<br>
О тебе ее устав,<br>
А тебя он осуждает<br>
Ожидать иных забав.<br>
<br>
5. Ничего со мной не тратишь,<br>
Истребляя жар в крови,<br>
Кроме нежности и сердца<br>
Постоянного в любви.<br>
Сим не можешь быть довольна,<br>
А иных достоинств нет.<br>
Лучшей части ты достойна,<br>
Ожидай ее, мой свет.<br>
