  Слова Федора Глинки<br>
  На голос: <em>Веселися в чистом поле </em><br>
  <br>
  Вспомним, братцы, россов славу <br>
  И пойдем врагов разить! <br>
  Защитим свою державу: <br>
  Лучше смерть - чем в рабстве жить. <br>
  <br>
  Мы вперед, вперед, ребята, <br>
  С Богом, верой и штыком! <br>
  Вера нам и верность свята: <br>
  Победим или умрем! <br>
  <br>
  Под смоленскими степями, <br>
  Здесь, России у дверей, <br>
  Стать и биться нам с врагами!.. <br>
  Не пропустим злых зверей! <br>
  <br>
  Вот рыдают наши жены, <br>
  Девы, старцы вопиют, <br>
  Что злодеи разъяренны <br>
  Меч и пламень к ним несут. <br>
  <br>
  Враг строптивый мещет громы, <br>
  Храмов Божьих не щадит; <br>
  Топчет нивы, палит домы, <br>
  Змеем лютым в Русь летит! <br>
  <br>
  Русь святую разоряет!.. <br>
  Нет уж сил владеть собой: <br>
  Бранный жар в крови пылает, <br>
  Сердце просится на бой! <br>
  <br>
  Мы вперед, вперед, ребята, <br>
  С Богом, верой и штыком! <br>
