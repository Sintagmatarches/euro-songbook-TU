        телеэфире, спел ее на финальном концерте Большой детский хор, который 
        толком даже не успел песню выучить; солировала в хоре Инна Курилова. Саму Ведищеву 
        на церемонию не пригласили. В дальнейшем песню запретили и сняли с эфира. 
