	  В 1970 году Сергей Юткевич снимал на Мосфильме авангардную картину &quot;Феерическая комедия&quot; по мотивам пьесы Маяковского &quot;Клоп&quot;. Это был первый опыт совместной работы Владимира Дашкевича и Юлия Кима. Из-за инфаркта Юткевича съемки были остановлены. В 1975 году комедия вышла в сокращенном варианте под названием &quot;Маяковский смеется&quot; (режиссеры Сергей Юткевич и Анатолий Каранович). Песня Кима существует в разных вариантах - в длинном, 1970 года и в укороченном, вошедшем в окончательную редакцию картины. Песню поет главный герой Присыпкин, в роли которого дебютировал Юрий Чернов. <br>
