<h1>ЦВЕТОК</h1>

Музыка Михаила Петренко<br>
Слова Владимира Крахта<br>
<br> 
1. Часто перед нами пестрой вереницей<br>
В памяти мелькают юности года.<br>
Забываешь встречи, забываешь лица,<br> 
Но одно запомнишь в жизни навсегда.<br>
Чей-то теплый голос, как весны дыханье,<br>
Будет сердце к счастью, к молодости звать.<br>
И опять при встрече, как при расставаньи,<br>
Ласково, любовно хочется сказать:<br> 
<br>
<span class='i'>Припев:<br></span>
<br>
Мне не забыть этот вечер, <br>
Как бы он ни был далек.<br>
Что подарить Вам на память о встрече -<br> 
Возьмите вот этот цветок.<br>
<br>
2. Отшумели годы, пронеслись тревоги,<br>
Расцветает снова каждый уголок.<br>
И в походной сумке попадется многим<br>
В пачке старых писем высохший цветок.<br>
Пожелтеют листья, потускнеют строки,<br>
И сжимая письма в дрогнувшей руке,<br>
Вспомним мы, когда-то в час войны жестокой<br>
Пел нам тёплый голос песню о цветке.<br>

