Музыка Модеста Табачникова<br>
Слова Григория Гридова<br>
<br>
Мама — нет слов ярче и милей. <br>
Мама — нет глаз мягче и добрей. <br>
Хорошо мне жить любя, мама родная, <br>
Посмотрю я на тебя — ведь ты совсем седая. <br>
Сердце свое мне ты отдала, <br>
Сколько ночей сон мой берегла! <br>
Но согрею я теперь старость твою, <br>
Для тебя сегодня я пою! <br>
<br>
<div style="margin-left:32px">Мама, кто на свете тебя родней! <br>
Мама, что теплее любви твоей! <br>
Песня колыбельная, ласка беспредельная, <br>
Радость моих долгих-долгих дней! <br>
Мама, как всегда мне светло с тобой. <br>
Мама, всюду вижу я образ твой. <br>
В горе — молчаливая, в праздник — хлопотливая. <br>
Мама! Милая мама! </div>
<br>
Если, мой друг, ты недалеко, <br>
Всё мне ясней, на душе легко. <br>
Любишь вспомнить иногда ты в час покоя <br>
Мои юные года, детство золотое. <br>
Вспомнишь и вдруг отведешь глаза, <br>
Тихо к губам катится слеза. <br>
Можно в радости всплакнуть, если полней <br>
Стало счастье у твоих детей.<br>
<br>
<div style="margin-left:32px">Мама, как всегда мне светло с тобой. <br>
Мама, всюду вижу я образ твой. <br>
В горе — молчаливая, в праздник — хлопотливая. <br>
Мама! Милая мама! </div>
