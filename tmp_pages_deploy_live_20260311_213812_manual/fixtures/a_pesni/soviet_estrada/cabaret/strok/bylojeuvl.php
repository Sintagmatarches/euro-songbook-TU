<br>
&quot;Написано специально для К.И. Шульженко. К 1944 г. (после выступлений на 
ленинградском фронте и в блокадном Ленинграде), оркестр, сопровождавший певицу 
(п/у трубача Алексея Семенова) переезжает в Казахстан и Ср. Азию. Строк в это 
время находится в эвакуации в Алма-Ате, где работает пианистом на киностудии (сюда 
эвакуированы и здесь временно объединены «Мосфильм» и «Ленфильм»). С приездом 
Шульженко, являвшейся худруком оркестра Семенова, Строк начинает работать в этом 
коллективе. «Былое увлечение» — последнее танго Строка, получившее известность 
еще до опалы композитора. В 1944—45 гг. Строк живет в Москве, сотрудничает с Утесовым, 
Цфасманом. Затем по настоянию сына возвращается в Ригу. Начинается холодная война, 
сопровождаемая кампаниями по борьбе с низкопоклонством перед Западом, безыдейностью, 
упадничеством, иностранщиной, «чуждыми влияниями», формализмом, космополитизмом. 
Под обстрел попадают многие исполнители и авторы джазовой и легкой музыки. Строк 
— удобная мишень. Стараниями некоторых функционеров (публикации в прессе и т. 
п.) в декабре 1948 г. Строк был исключен из Союза композиторов Латвии (одно из 
выдвигавшихся обвинений — непрофессионализм). С этого момента Строк до конца 60-х 
гг. становится «фигурой умолчания»&quot;. <br>
