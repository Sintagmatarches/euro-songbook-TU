  Сафошкин<br></span>
  <h1>ЗВЕЗДА МОИХ ПОЛЕЙ, ЗВЕЗДА МОЕЙ РОССИИ... </h1>
  <span class='i'>Я люблю тебя, жизнь: Песни на все времена / Сост. Л. Сафошкина, В. Сафошкин. 
  М.: Эксмо, 2004. С. 131-140.<br></span>
  <br>
  <br>
  <img src="a-zvezdam.jpg" alt="Сергей Лемешев"><br>
  <br>
  <br>
  "...Исполняет Сергей Яковлевич Лемешев". Знакомые, а когда-то привычные слова. 
  Дальше - короткая пауза и... Звучный и одновременно нежный, неповторимый лемешевский 
  голос наполняет собой воздушное пространство, с первых же нот проникая в сердца 
  очарованных слушателей. <br>
  <br>
  "...Серебристый голос драгоценного сплава лился словно сам собой, вызывая одновременно 
  чувство радости и грусти, - так описывает впечатление от пения Лемешева народный 
  артист СССР, дирижер Большого театра Б. Хайкин. - Оба эти, казалось бы, противоречивые 
  чувства гармонично сливаются и как бы дополняют друг друга... <br>
  <br>
  ...Невозможно себе представить звучание русских песен более яркое, более взволнованное, 
  берущее за душу, чем в исполнении Лемешева". <br>
  <br>
  Да, Сергей Яковлевич Лемешев был замечательным, не знающим себе равных исполнителем 
  народной песни - русской, украинской, неаполитанской. Но разве только песни? 
  Это благодаря ему, его голосу, его вокальному искусству миллионы людей, далеких 
  от "серьезной" музыки, узнали и полюбили оперу и классический романс. Певец 
  величайшего артистизма и обаяния, он сделал популярными многие яркие произведения 
  советских композиторов-песенников. <br>
  <br>
  Музыкант, певец, чье творчество всегда было народным по своей сути, Лемешев 
  и воспринимался своими современниками как частичка народа, плоть от плоти русской 
  земли. В этом утверждении нет ни малейшего преувеличения, ведь Лемешев и появился 
  на свет в деревеньке, затерявшейся в глухих тверских лесах. Там, в селе Старое 
  Князево, в семье батрака и прошли детство и отрочество будущего корифея советской 
  музыки. "Веселых песен мне запомнилось мало", - напишет потом в своих автобиографических 
  записках Сергей Яковлевич, вспоминая о годах, прожитых впроголодь, однообразном 
  бедняцком быте. Однако череда суровых будней не заслоняет от глаз впечатлительного 
  мальчика доброй красы среднерусской природы, от его чуткого уха - красивого 
  наигрыша, льющейся над полями песни... <br>
  <br>
  Безусловную музыкальную одаренность голосистого крестьянского паренька замечают 
  в семье архитектора Николая Александровича Квашнина, поселившейся в 1918 году 
  на соседнем со Старым Князевым хуторе. Жена Квашнина, профессиональная певица, 
  берется за музыкальное образование Сергея и... в течение года подготавливает 
  с ним знаменитую арию Ленского. С этой арией, а также еще несколькими вокальными 
  номерами, отрепетированными в доме Квашниных, семнадцатилетний Лемешев пешком 
  приходит в Тверь (за пятьдесят верст) и появляется в рабочем клубе Третьего 
  Интернационала. На дворе уже декабрь, но на госте из глубинки всего лишь "холодные" 
  бумажные брюки и куцый ватничек. Обаятельно улыбаясь, паренек хватает кого-то 
  за рукав. <br>
  <br>
  - Вам что, товарищ?<br>
  <br>
  - У вас тут концерт... <br>
  <br>
  - Ну и что?.. <br>
  <br>
  - Я... Я бы хотел выступить... <br>
  <br>
  Дебют молодого Лемешева прошел более чем удачно, его вызывали "на бис", долго 
  не отпускали со сцены. Эпизод появления Ленского из народа в рабочем клубе будет 
  потом использован создателями популярнейшего фильма с Лемешевым в главной роли. 
  "Музыкальная история" вышла спустя более десяти лет после подлинных событий, 
  к тому же многое в фильме - и место, и участники, да и сами события - было изменено. 
  Но зрители все равно узнавали в таксисте Пете Говоркове черты своего любимого 
  певца Сергея Яковлевича Лемешева... Волшебная сила кино удвоила, а то и утроила 
  популярность певца. <br>
  <br>
  ...По счастью, в реальной жизни публичное выступление Лемешева прошло более 
  чем успешно. Небольшой, битком набитый рабочий клуб горячо аплодировал ариям 
  Ленского, Надира и Левко в исполнении молодого тверчанина. А когда, согревая 
  зрительские сердца, в промерзший зал полилась раздольная русская песня, слушатели 
  пришли в такой восторг, что еще долго не отпускали со сцены смущенного дебютанта... 
  <br>
  <br>
  Не раз еще припомнит добрым словом Сергей Яковлевич своих наставников и друзей 
  Квашниных. Эти замечательные люди, настоящие русские интеллигенты, сыграли в 
  его судьбе примерно ту же роль, что сельские педагоги Власовы в жизни юного 
  Сергея Есенина. Квашнины открыли перед любознательным сельским пареньком огромный 
  и сложный мир искусства, научили ценить свой талант, приохотили к книгам. Привитая 
  ими любовь к чтению помогла молодому Лемешеву и впоследствии - при поступлении 
  в Тверское кавалерийское училище. Оценив хорошие знания абитуриента в русской 
  литературе, экзаменаторы простили Сергею пробелы в других отраслях знаний... 
  <br>
  <br>
  Став курсантом Тверских кавкурсов, Лемешев продолжает занятия вокалом - поступает 
  учиться в музыкальную школу, время от времени появляется на самодеятельных сценических 
  подмостках. Постоянный и неизменный успех, сопутствовавший его выступлениям, 
  укрепляет в правильности выбранного пути, вселяет в душу твердую уверенность: 
  "Буду певцом!". В этом стремлении молодой талант поддерживают и городские власти. 
  <br>
  <br>
  Ходатайство губернского "подотдела искусств" удовлетворяется начальником училища 
  - седоусым Никифоровым, заядлым конником и большим поклонником лемешевского 
  пения. В сентябре 1921 года курсант Лемешев уезжает в Москву - сдавать экзамены 
  в Московскую консерваторию. <br>
  <br>
  "Едва приехав в Москву, - вспоминает артист, - мчусь в Большой театр. Идет "Евгений 
  Онегин". Ленский - Собинов! Сбылась моя заветная мечта: на галерке, зажатый 
  в толпе таких же рьяных поклонников великого артиста, я наслаждался пением Собинова". 
  <br>
  <br>
  Консерватория, Большой театр, мастерство Собинова производят на молодого провинциала 
  такое сильное впечатление, что на прослушивание он является в смятенных чувствах 
  и проходит его ниже своих возможностей - по крайней мере так ему кажется. Почти 
  уверенный, что провалил экзамен, Лемешев все-таки приезжает на следующий день 
  в Консерваторию и с тяжелым сердцем подходит к вывешенным на доску спискам. 
  <br>
  <br>
  "...Я был уверен, что моей фамилии там нет! - вспоминает певец об этой "роковой" 
  в своей жизни минуте. - Стою перед доской и не смотрю... Потом, наконец, собрался 
  с силами, глянул - мелькнуло что-то знакомое, всмотрелся уже внимательнее: принят! 
  - запело в душе!" <br>
  <br>
  Позади - экзаменационная пора, кружащая голову радость победы. Начинается учеба. 
  Занятия в Консерватории требуют от Лемешева полной отдачи сил - он еще многого 
  не знает или же знает понаслышке. Лемешев учится жадно, взахлеб, не обращая 
  внимания на бытовые неурядицы. В стране догорает огонь гражданской войны, не 
  хватает самого необходимого. Маленькую консерваторскую стипендию выдают натуроплатой 
  - пшеничной мукой, приходится <br>
  по три раза в день печь себе блины - единственную в то время его еду... И все-таки 
  Лемешеву хорошо как никогда - ведь он занимается тем, что должно стать делом 
  всей его жизни. И как не петь душе, сопричастной великому чуду сотворения музыки, 
  постоянно соприкасающейся с лучшими ее образцами... Все вечера Лемешев теперь 
  проводит в Большом, слушая А. Пирогова, Д. Головина, С. Мигая... В это же время 
  ему выпадает счастье услышать самого Ф. И. Шаляпина, и еще долгое время он будет 
  находиться под обаянием могучего голоса и бесподобной игры великого оперного 
  артиста. <br>
  <br>
  Сценическое мастерство Шаляпина считается эталонным и в оперной студии К.С. 
  Станиславского, куда Лемешев поступает после окончания Консерватории. Вновь 
  - теперь уже под руководством прославленного режиссера - Лемешев берется за 
  роль Ленского. <br>
  <br>
  Партией Ленского дебютирует молодой певец на сцене одного из ведущих оперных 
  театров страны - Свердловского. Покорив северян легкостью и ласкающим лиризмом 
  своего голоса, природной пластичностью, скупой выразительностью сценического 
  жеста, Лемешев в течение одного сезона представляет на суд зрителя еще одиннадцать 
  работ - ответственных ролей первого плана... И все-таки Ленский остается лучшей 
  и любимой ролью певца. <br>
  <br>
  "...Ленский - Лемешев... 18-летний юноша с густыми кудрями, с порывистыми приемами 
  молодого поэта а-ля Шиллер, - напишет в 1927 году рецензент эмигрантской газеты 
  в Харбине, после того как увидит Лемешева на сцене тамошней русской оперы. - 
  Прекрасно была проведена ария перед дуэлью, трудная для исполнения, дуэт в той 
  же картине, но особенно удалась артисту сцена на балу у Лариных, "В вашем доме", 
  где он блеснул дивным пиано..."<br>
  <br>
  Два успешных сезона на сцене русской оперы в Харбине, два - в Тбилисской опере, 
  известной своими превосходными певцами. За эти годы выросло вокальное и сценическое 
  мастерство Лемешева, расширился его репертуар. А главное, стала близка к осуществлению 
  заветная мечта его молодости: работа в главном оперном театре страны - Большом... 
  <br>
  <br>
  "...Большой театр всегда был для меня недосягаемой мечтой, храмом, под священную 
  сень которого могли вступить только самые достойные! Помню, как, выступая в 
  концерте, я долго не мог овладеть собою от волнения, что меня представили публике 
  как солиста Большого театра!" <br>
  <br>
  Право называться солистом Большого театра Сергей Яковлевич получает в феврале 
  1931-го, спев царя Берендея в "Снегурочке" и Джеральда в "Лакме", и с тех пор 
  его творчество, вся его жизнь будут связаны с Большим. К 30-40-м годам наступает 
  расцвет концертной деятельности Лемешева: он подготавливает и исполняет в течение 
  пяти вечеров все романсы Чайковского, а это около ста произведений на слова 
  десятков авторов. Уникальность этого беспримерного в истории вокального искусства 
  опыта заключается еще и в том, что исполнителю потребовалось приспособить для 
  голоса произведения, написанные для баса и меццо-сопрано. <br>
  <br>
  "Но Сергей Яковлевич все преодолел, - свидетельствует в своих воспоминаниях 
  замечательная русская певица Н. Казанцева, партнерша Лемешева на сцене Большого 
  театра, - и многие романсы, сколько бы я потом ни слышала их в другом исполнении, 
  вспоминаются мне только в звучании лемешевского голоса". <br>
  <br>
  В колдовском звучании голоса Лемешева миллионам людей нескольких поколений навсегда 
  запомнились романсы "Я помню чудное мгновенье", "Средь шумного бала", лишь один 
  раз услышанные, они на- всегда остались в сознании как лемешевские: "На заре 
  туманной юности", "Вдоль по улице метелица метет", "Скажите, девушки", "Ах ты, 
  душечка". А сколько прекрасных старинных русских песен его талант вывел из забвения, 
  вернул любителям музыки! Скольким новинкам, вошедшим потом в классику эстрадной 
  песни, открыл дорогу... В лемешевские времена не принято было делить концертную 
  эстраду на две части, лучшую и большую из них отдавая бессодержательным, безвкусным 
  поделкам... Для исполнения с эстрады писали музыку такие композиторы, как Мясковский, 
  Новиков, Хренников, а среди исполнителей легкого музыкального жанра были имена 
  М. Рейзена, С. Преображенской, А. Пирогова. Не случайно в годы Великой Отечественной 
  фронтовые патефоны крутили - наряду с "Синим платочком" К. Шульженко, "Валенками" 
  Л. Руслановой и "Мишкой-одесситом" Утесова - также и пластинки с записями С. 
  Лемешева. <br>
  <br>
  "Необычайный слушатель пришел в наш театр, - писал в одной из газетных статей 
  военного времени С. Я. Лемешев, - на спектакли приезжали зенитчики Подмосковной 
  зоны, летчики, только что сбившие вражеские машины, люди, целый день рывшие 
  противотанковые рвы, девушки с оборонных заводов. И эти суровые люди, многие 
  из которых не раз смотрели в глаза смерти, слушали музыку Чайковского и Верди 
  как-то по-особенному взволнованно... <br>
  <br>
  Во время тревоги публика почти вся оставалась в зрительном зале, хотя рядом 
  с театром есть... прекрасное бомбоубежище. Зрители терпеливо ждали продолжения 
  спектакля. Отбой... И снова гремит оркестр и по сцене плывут в вальсе гости 
  Лариных". <br>
  <br>
  Выступления на сцене Большого, гастрольные поездки, концерты, грамзапись... 
  Напряженный творческий поиск не в силах остановить даже тяжелая болезнь легких, 
  настигшая певца в конце шестидесятых. Одолев тяжелый недуг, Лемешев снова выходит 
  на сцену. <br>
  <br>
  О том, что Лемешев выступит в Ленинграде с сольным концертом, я узнал от своего 
  друга - коллекционера грампластинок Николая Калмыкова. На вопрос Николая, смогу 
  ли приехать, отвечаю не задумываясь: <br>
  <br>
  - Буду обязательно! Лишь бы были билеты. <br>
  <br>
  Билетов, естественно, не было, но Коля сделал невозможное, и в урочный час мы 
  с волнением и трепетом занимали свои места в зале Ленинградской филармонии. 
  Волновались еще и потому, что прекрасно знали о перенесенной Лемешевым болезни. 
  Вопросы "в какой форме Сергей Яковлевич" и "сохранил ли голос" буквально витали 
  в наэлектризованном тревожным ожиданием воздухе. <br>
  <br>
  И вот Лемешев снова на сцене. Прозвучала первая нота, затем - первая музыкальная 
  фраза... Облегченный вздох проносится по рядам зрителей - голос Лемешева так 
  же свеж и гибок, как в лучшие его годы. Что же касается интерпретации... В исполняемых 
  им песнях и романсах на музыку Л. Бетховена и Ф. Шуберта, П. Чайковского и Н. 
  Римского-Корсакова проникновенный лиризм и яркость красок подчеркнуты драматической 
  глубиной образа. С каждой минутой зрители убеждаются, что присутствуют при рождении 
  новой трактовки камерной классики. <br>
  <br>
  По окончании концерта, потрясенные услышанным, проталкиваемся к окруженному 
  зрителями певцу. Сбивчиво, с пятого на десятое, выражаем ему свой восторг... 
  Узнав, что перед ним коллекционеры, Сергей Яковлевич дарит каждому по комплекту 
  долгоиграющих пластинок со своими записями и автографом. <br>
  <br>
  О событиях того вечера мне живо напоминает еще и серия фотографий, сделанных 
  на концерте известным ленинградским фотомастером В. Никитиным. На всякий случай 
  я запасся двумя комплектами, и ближайшее же будущее показало, что чутье меня 
  не обмануло. <br>
  <br>
  10 апреля 1973 года уже в Москве в Большом зале Консерватории состоялся концерт 
  Лемешева, на котором певец исполнил цикл романсов на музыку М. Глинки, Н. Римского-Корсакова 
  и Д. Шостаковича. По окончании концерта я с букетом цветов зашел в гримерную 
  к Сергею Яковлевичу и, напомнив о концерте двухгодичной давности, навел разговор 
  на никитинские фотографии. Сергей Яковлевич признался, что хотел бы иметь хотя 
  бы несколько снимков, и я, внутренне ликуя, вручил ему весь комплект. <br>
  <br>
  Лемешев, разумеется, не остался в долгу и тут же подписал мне на память свою 
  книгу со знаменательным названием - "Мой путь в искусство". <br>
  <br>
  Когда сейчас я вспоминаю Сергея Яковлевича Лемешева, на память невольно приходит 
  фраза, божественно петая им: "Не счесть алмазов, не счесть жемчужин..." в душе 
  и сердце этого великого Мастера. <br>
  <br>
  Выступая в Большом театре на вечере в честь 70-летия С.Я. Лемешева, его директор 
  Борис Покровский говорил: <br>
  <br>
  "Очень важно понять, суметь ответить на вопрос, а что такое настоящий народный 
  артист?<br>
  <br>
  Я думаю, что самым коротким, самым точным ответом на это будет одно слово: ЛЕМЕШЕВ!"<br>
  <br>
  Согласитесь, лучше не скажешь... <br>
