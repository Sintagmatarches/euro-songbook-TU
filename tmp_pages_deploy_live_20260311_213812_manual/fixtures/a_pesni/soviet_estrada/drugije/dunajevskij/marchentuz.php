  Несокрушимая моя.<br>
  <br>
  <em>Припев:</em><br>
  <br>
  Нам нет преград ни в море, ни на суше,<br>
  Нам не страшны ни льды, ни облака.<br>
  Пламя души своей, знамя страны своей<br>
  Мы пронесем через миры и века!<br>
  <br>
  Нам ли стоять на месте!<br>
  В своих дерзаниях всегда мы правы,<br>
  Труд наш - есть дело чести,<br>
  Есть дело доблести и подвиг славы.<br>
  К станку ли ты склоняешься,<br>
  В скалу ли ты врубаешься, -<br>
  Мечта прекрасная, еще неясная,<br>
  Уже зовет тебя вперед.<br>
  <br>
  Создан наш мир на славу.<br>
  За годы сделаны дела столетий,<br>
  Счастье берем по праву,<br>
  И жарко любим, и поем, как дети.<br>
  И звезды наши алые<br>
  Сверкают, небывалые,<br>
  Над всеми странами, над океанами<br>
  Осуществленною мечтой.<br>
  <br>
  <em>Русские советские песни (1917-1977). Сост. Н. Крюков и Я. Шведов. 
  М.: Худож. лит.&quot;, 1977. Дата: 1936</em>.<br>
   <br>
  <br>
  В сборнике песня датирована 1936 годом - видимо, в этом году написано стихотворение; фильм вышел на экраны в 1940 году. Картина посвящена 
  стахановскому движению в ткацкой промышленности, время действия - середина 1930-х 
  гг. Героиня - поначалу безграмотная деревенская девушка Таня Морозова (в исполнении 
  Любови Орловой) становится стахановкой (в этот момент по сюжету лето 1935 г. 
  - начало стахановского движения), ставит рекорд , едет в Москву, получает Орден 
  Ленина, и параллельно обретает счастье в личной жизни. В фильм песня вошла фрагментами 
  и не в такой редакции, в исполнении Любови Орловой. Полностью прозвучали второй 
  куплет (4-я ст.: <em>&quot;Есть подвиг доблести и подвиг славы&quot;</em>) и припев - 
  в сцене, когда Таня Морозова устанавливает рекорд. Всего в фильме около десятка 
  песен и песенок, <em>&quot;Марш энтузиастов&quot; </em>- центральная.<br>
  <br>
  Песня была очень популярна в годы Великой Отечественной войны.<br>
