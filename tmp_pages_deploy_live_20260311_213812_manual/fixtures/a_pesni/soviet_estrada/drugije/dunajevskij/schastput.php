  Одноактная комедия (джаз-клоунада) &quot;Музыкальный магазин&quot; написана 
  Николаем Эрдманом и Владимиром Массом для Теа-джаза Леонида Утесова, который 
  тогда только начинал приобретать популярность (был это 1932 г. или чуть раньше). 
  Несколько музыкальных вставок написал Дунаевский. В 1932 и 1933 гг. четыре номера 
  из клоунады были записаны на пластинку – пародия-шутка &quot;Сердце красавицы&quot; 
  (на основе песенки Герцога из оперы &quot;Риголетто&quot;), парафразы на тему 
  мелодий из опер &quot;Садко&quot; и &quot;Евгений Онегин&quot; и &quot;Счастливый 
  путь&quot; – одна из первых песен Дунаевского. В дальнейшем в течение долгого 
  времени эта песенка завершала концерты Утесова.<br>
