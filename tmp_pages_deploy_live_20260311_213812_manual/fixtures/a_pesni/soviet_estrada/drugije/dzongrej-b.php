	Блантер вспоминал, что песня написана им с Владимиром Массом весной 1923 года 
        и вскоре вышла на пластинке. Хронология немного не стыкуется. В 1923 году новые 
        пластинки в стране не записывались. Музпред начал записи весной 1924 года, 
        и в первом его каталоге (август 1924-го) &quot;Джон Грей&quot; не значится, то есть пластинки еще не было. Она упоминается в следующем каталоге, вышедшем в конце 1926 года. Историк российской звукозаписи Глеб Скороходов указывает, что пластинка была записана в 1926 году в исполнении оркестра под управлением виолончелиста Большого театра Станислава Корта в Кабине напевов (т.е. студии звукозаписи).<br>
		<br>
		Владимир Масс в 1933 году был арестован, десять лет провел в ссылке, а его имя на эти годы было вычеркнуто из официальных источников, в том числе из титров фильма &quot;Веселые ребята&quot;, соавтором сценария которого был Масс. <br>
        <br>
        <br>
        <strong>Из воспоминаний Матвея Блантера в беседе с журналистом Глебом Скороходовым в конце 1960-х гг.: </strong><br>
        <br>
        <em>&quot;О пластинке в то время мечтать было нечего – их делали очень мало. Мне повезло, в начале 20-х моего «Джона Грея» записал Музпред или Музтрест, уже не помню. И этот фокстрот побил рекорд популярности. Когда в 24-м году Арго, Адуев, Типот и Вера Инбер написали обозрение против Чемберлена и других политиков и прочли его в Реввоенсовете, то председатель посоветовал обязательно использовать «Джона Грея», но с новым текстом, злободневным. И Вера Инбер написала какую-то чушь собачью: «Это необыкновенно – веер для Коминтерна!» <br>
        <br>
После «Джона Грея» что бы я ни писал, на пластинки это не попадало – там начал править дьявольский РАПМ. А сочинял я немало.<br>
        <br>
В девятнадцатом году для знаменитого Бориса Борисова написал «Мальчишку шлепнули в Иркутске». Вы бы слышали, как он это исполнял! Не зря же его обожал Шаляпин, и Борисов пел для него в бенефис «Фатеницу».<br>
        <br>
А «Джон Грей» родился случайно. Я устроился заведовать музыкальной частью в театр к Форрегеру – «Мастфор». Там собралась неплохая компания: зав. литчастью – Владимир Масс, главный художник – Сергей Юткевич, будущий кинорежиссер, второй художник – Сергей Эйзенштейн, тоже о кино еще не помышлявший. Форрегер блестяще ставил обозрения, в которые вставлял музыкальные номера, преимущественно иностранные шлягеры. Все говорили, что делал это он гениально. И я как-то сказал Володе Маасу:<br>
        <br>
- Давай сами напишем что-нибудь в этом духе, что мы, лаптем щи хлебали?!<br>
<br>
И весной двадцать третьего появился тот самый «Грей», а затем и пластинка. С эстрады тогда меня пели много. Антонина Васильевна Нежданова исполняла в концертах мою «Фудзияму», но на пластинки ее не записала. Вообще в грамстудию меня не приглашали лет пятнадцать!<br>
        </em><br>
        (Скороходов Г. А. Тайны граммофона: Все неизвестное о пластинках и звездах грамзаписи. - М.: Изд-во Эксмо, Изд-во Алгоритм, 2004, с. 73-74). <br>
        <br>
        <br>
        <strong>В другой главе той же книги, с. 264-266, уже текст самого Скороходова:</strong><br>
        <br>
        <img src="../drugije/skoroh1.jpg" width="95" height="117"><br>
        <br>
        <em> &quot;В своих пристрастиях Объединение музыкальных предприятий проявило удивительную преемственность. В его «Полном каталоге пластинок для граммофона», выпущенном в конце 1926 года, на ста страницах названо около трех тысяч записей, вдвое больше, чем в предыдущем. Новинки среди них представляют наибольший интерес… Впервые на русской этикетке появилось это мало кому знакомое английское слово «фокстротт» (так оно писалось в ту пору). Корт и Львов-Вельяминов разделили пальму первенства в исполнении первых танцев в новом ритме, вышедших на советских дисках. <br>
        <br>
Сегодня подобный факт воспринимается в порядке вещей, и ничего необычного в нем нет. Тогда же, в 1926 году исполнение ансамблем Корта первого советского фокстрота «Джон Грей» Матвея Блантера вызвало бурю. Для членов Российской ассоциации пролетарских музыкантов «Джон Грей» надолго стал красной тряпкой их корриды борьбы с искусством, чуждым массам, а сам новый ритм - зловреднее уголовщины. И хотя со страниц изданий «создателей пролетарской музыки» посыпались проклятия и композиторам-нэпманам, к которым тут же был причислен Блантер, получивший титул «фокстротчика», и Музпред с Апрелевкой, попавшие под влияние нэпманских вкусов, на брань РАПМа, только набиравшего силу, не обратили внимания. <br>
        <br>
Более того, Музпред приглашает в свой кабинет напевов духовой оркестр под управлением Федора Николаевского, тоже артиста Большого театра, кларнетиста, композитора, впоследствии возглавившего один из лучших коллективов духовиков страны. Его ансамбль исполнил в один присест серию произведений в переложении для духовых инструментов — «Варшавянку», «Смело, товарищи, в ногу», похоронный марш «Вы жертвою пали», «Траурный марш» Геде, а потом, быть может, для того, чтобы взбодрить дух — сразу два бравурных фокстрота «Электрик Джимми» Блантера и «Эвелин» сочинения руководителя оркестра. <br>
        <br>
Десяток фокстротов на три тысячи других записей, попавших в упомянутый каталог 1926 года. Скромно. Но рапмовцы восприняли их брешью, сквозь которую хлынет мутный поток заграничной заразы&quot;. </em><br>
