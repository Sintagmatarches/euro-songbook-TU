<h1>КОМСОМОЛЬЦЫ-ДОБРОВОЛЬЦЫ</h1>

  Музыка Марка Фрадкина<br>
Слова Евгения Долматовского<br>
<br>
Хорошо над Москвою-рекой<br>
  Услыхать соловья на рассвете.<br>
  Только нам по душе непокой,<br>
  Мы сурового времени дети.<br>
  <br>
  <span class='i'>Припев:</span><br>
  <br>
  Комсомольцы-добровольцы,<br>
  Мы сильны своей верною дружбой.<br>
  Сквозь огонь мы пройдем, если нужно<br>
  Открывать молодые пути.<br>
  Комсомольцы-добровольцы,<br>
  Надо верить, любить беззаветно,<br>
  Видеть солнце порой предрассветной -<br>
  Только так можно счастье найти.<br>
  <br>
  Поднимайся в небесную высь,<br>
  Опускайся в глубины земные.<br>
  Очень вовремя мы родились,<br>
  Где б мы ни были - с нами Россия.<br>
  
  <br>Лучше нету дороги 
  такой,<br>
  Все, что есть, испытаем на свете,<br>
  Чтобы дома над нашей рекой<br>
  Услыхать соловья на рассвете.<br>
  <br>
  1957<br>
  <br>
<span class='i'>Русские советские 
  песни (1917-1977) / Сост. Н. Крюков и Я. Шведов. М.: Худож. лит., 1977.</span> <br>
