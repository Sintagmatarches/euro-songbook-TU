режиссер-постановщик Эльдар Рязанов, 1980<br>
<br>
Музыка Андрея Петрова<br> 
Слова Михаила Савоярова
<br>
<br>
По селу бегут мальчишки,<br>
Девки, бабы, ребятишки,<br>
Словно стая саранчи…<br>
В трубы дуют трубачи:<br>
Та-та-та-та!<br>
Раздаются тары-бары:<br>
К нам приехали гусары!<br>
Все красавцы усачи!<br>
В трубы дуют трубачи:<br>
Па-па-па-ра!<br>
<br>
Слышен голос командира:<br>
- Размещаться по квартирам!<br>
Дело близится к ночи,<br>
В трубы дуют трубачи:<br>
Та-та-та-та!<br>
В эту ноченьку немало<br>
Баб с солдатами шептало:<br>
- Без тебя хоть, милый, плачь,<br>
Потруби и мне, трубач:<br>
Па-па-па-ра!<br>
<br>
А когда зорю сыграли,<br>
Бабы слезы утирали,<br>
И, в котомку взяв харчи,<br>
Уходили трубачи:<br>
Та-та-та-та!<br>
Через год в каждой избенке<br>
Народилось по мальчонке,<br>
Глотки драли, как сычи,<br>
Тоже будут трубачи:<br>
Па-па-па-ра!<br>
