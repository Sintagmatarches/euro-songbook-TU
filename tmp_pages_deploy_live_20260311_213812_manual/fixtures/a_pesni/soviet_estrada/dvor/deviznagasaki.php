  <br>
  <strong>ВАРИАНТЫ (4) <br>
  <br>
  1. Девушка из Нагасаки</strong><br>
  <br>
  Песня о девушке из Нагасаки на самом деле не относится к числу чисто блатных. 
  Текст написан в конце 20-х годов поэтессой Верой Инбер (1) на музыку Поля Марселя. 
  Песня была популярна среди уголовников наряду с множеством других произведений 
  «морской романтики» (<a href="vkejptaunskom.php">«В Кейптаунском порту»</a>, 
  <a href="jungabill.php">«На корабле матросы ходят хмуро»</a>, <a href="vnachugavan.php">«В 
  нашу гавань заходили корабли»</a> и пр.) В босяцкой среде было много бывших 
  матросов, значительно влияние моряцкой субкультуры на уголовный мир (сюжеты 
  татуировок, морские словечки в блатном жаргоне и т. д.) <br>
  <br>
  Но я включил именно эту песню в сборник не поэтому. Свою роль сыграл случай, 
  рассказанный Валерием Приёмыховым. Актёр вспомнил, как на съёмках одного из 
  фильмов нужно было исполнить какую-нибудь «классическую» уркаганскую песню. 
  Съёмочная группа отправилась в Бутырскую тюрьму. Приёмыхова с товарищами провели 
  по множеству камер, где он беседовал с арестантами. Каково же было удивление 
  артиста: практически никто из обитателей камер не знал ни одной настоящей лагерной 
  песни! Пели в основном Высоцкого, Розенбаума или какую-нибудь современную низко 
  пробную «блатоту». И лишь в одной из камер старый замшелый «сиделец» исполнил, 
  путаясь и сбиваясь, «блатной фольклор» — «Девушку из Нагасаки»...<br>
