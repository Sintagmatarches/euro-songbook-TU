Автором музыки часто называют Поля Марселя (Павла Александровича Русакова, 1908-1973), однако без доказательств. Поль Марсель родился  в Марселе в семье эмигрантов-евреев из Ростова, затем семью выслали в  Россию как неблагонадежный элемент. В  СССР  закончил консерваторию по классу фортепиано и теории композиции. Некоторые источники называют его автором песни <a href="../drugije/druzba.htm">&quot;Дружба&quot;</a>. Арестован в 1937 г., получил 10 лет, отправлен в Вятлаг. Вышел в январе 1947 г., реабилитирован в 1956 г. Работал музыкальным руководителем и дирижером Ленинградского цирка.        <br>
        <br>
Весной 2011 г. проблема авторства Поля Марселя обсуждалась на форуме <a href="http://shanson-e.tk/forum/showthread.php?t=99893&highlight=%E2%E5%F0%F2%E8%ED%F1%EA%E8%E9" target="_blank">&quot;Музыкальный огонек&quot;</a>. 4 апреля пользователь Евгений933 поместил такой пост: <br>
<br>
&quot;... авторство Поля Марселя, как композитора можно снимать.
Вот что пишет &quot;Театральный журнал&quot; (№ 7 от 22 декабря 1918 г.) по поводу открытия 3 ноября 1918 г. подвала актеров «Красный кабачок» в Харькове (Сумская, 6):
«Откуда-то звенит гитара. Пьяная, нежная гитара де Лазари. «Шарабан мой, шарабан». Это Раисова. Сама Раиса Михайловна. «Он юнга, родина его Марсель». «Девушка из Нагасаки»......взяв скрипку у румына, играет некто «Чарочку». Музыка, вино, улыбки. Конечно, угар. Но если угорать вообще, так угорать радостно».
То есть, выходит, что в 1918 году песня уже была популярна, а Марселю в 18-м было всего лишь 10 лет&quot;.  <br>
