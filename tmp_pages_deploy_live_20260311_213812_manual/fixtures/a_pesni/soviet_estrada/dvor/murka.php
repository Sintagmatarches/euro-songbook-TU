<strong>Фима Жиганец<br>
<br>
  <span class="header">ЗДРАВСТВУЙ, МОЯ МУРКА...</span></strong><br>
<br>  
<em>Жиганец Ф. Блатная лирика. Сборник. Ростов-на-Дону: «Феникс», 
  2001, с. 46-58. (даны варианты: &quot;Мурка&quot; классическая с нотами, &quot;Маша&quot;, 
  &quot;Любка&quot;, &quot;Мурка&quot; московский вариант, &quot;Мурка&quot; подцензурная 
  и <a href="evrmurka.php">&quot;Сурка (&quot;Еврейская Мурка&quot;)</a>)<br></em>
  <br>
  Вершиной блатной песенной классики является знаменитая «Мурка». Не случайно 
  Валерий Леонтьев именно ею открыл новогоднюю программу НТВ-2000, посвящённую 
  самым популярным песням двадцатого века. История «Мурки» драматична и таинственна. 
  Именно поэтому я посчитал нужным в сборнике выделить для неё специальную главу. 
  <br>
  <br>
  Первоосновой песни о Мурке стала знаменитая в 20-е годы одесская песня о Любке-голубке. 
  Некоторые исследователи приписывают её авторство одесскому поэту Якову Ядову. 
  Однако с полной уверенностью утверждать этого нельзя. Константин Паустовский, 
  в 20-е годы работавший с Ядовым, писал в «Повести о жизни»: «Даже всеведущие 
  жители города не могли припомнить, к примеру, кто написал песню «Здравствуй, 
  моя Любка, здравствуй, дорогая...» <br>
  <br>
  В последнее время также появилась версия о том, что автором, музыки к «Мурке» 
  является замечательный композитор Оскар Строк, - что, впрочем, пока ничем не 
  подтверждено. <br>
  <br>
  В ранних вариантах песни «героиня» вовсе не выведена в качестве «авторитетной 
  воровки», каковой является в «классической» «Мурке». Наиболее близкий к первооснове 
  текст в этом сборнике — «Маша». Главный персонаж, Маша, помимо «бандитки первого 
  разряда», рисуется как любовница уркаганов («маша», «машка» на старой фене и 
  значило «любовница»). Однако в песне повествуется лишь о совместных кутежах, 
  нет даже упоминания о «воровской жизни» и о «речи» на «совете», а также о том, 
  что «бандитку» «боялись злые урки». Всё это пришло позже. Текст «Любки», приведённый 
  в нашем сборнике, по отношению к «Маше» является более поздним — и ни в коем 
  случае не первоначальной одесской «Любкой»! Оба текста — и «Маша», и «Любка», 
  — записаны в 1934 году студенткой Н. Холиной (хранятся в Центральном Государственном 
  Архиве Литературы и Искусства). К середине 30-х, таким образом, ещё сосуществовали 
  «Любка» и «Маша», позже, переименованная в Мурку. Метаморфоза произошла тогда, 
  когда, песня из Одессы вышла на широкие просторы, СССР и попала в столицу. Я 
  привожу здесь и «московский» довоенный вариант песни. Скорее всего, выбор нового 
  имени подсказала тема. В 20-е-40-е годы «мурками» называли работников Московского, 
  уголовного розыска (МУР). Существовала даже поговорка — «Урки и мурки играют 
  в жмурки», т. е. одни прячутся, другие ищут. В то же время Мурка — вариант имени 
  Мария, Маша. Таким образом, для уголовников имя Мурки стало воплощением гнусности 
  и подлости, которое в их представлении связывалось с коварными «ментами». (Впрочем, 
  вообще имя Мура в блатной среде, было чрезвычайно популярно). <br>
  <br>
  В результате многочисленных переделок «Любки» сначала в «Машу», потом — в «Мурку» 
  поздний текст песен оказался полон тёмных мест и противоречий. Например, речь 
  идёт о событиях, которые произошли не позднее 1922 года. Несколько раз упоминается 
  губчека, то есть губернская чрезвычайная комиссия по борьбе с контрреволюцией 
  и саботажем. Известно, что ВЧК с её отделениями на местах приказала долго жить 
  6 февраля 1922 года. Её функции были переданы ГПУ. Но в начале 20-х годов не 
  было Торгсина (магазинов по торговле с иностранцами, упоминаемых в некоторых 
  вариантах «Мурки»). Не было и «воров в законе». Между тем в песне специально 
  подчёркивается, что уркаганы боялись Мурки, потому что «воровскую жизнь она 
  вела». Кроме того, по воровским «законам», женщины не могли играть ведущей роли 
  в уголовном мире, а уж в сходках им вообще запрещалось участвовать, не говоря 
  о том, чтобы там «держать речь». Московский вариант с привнесением в текст агентов 
  МУРа вообще вносит, казалось бы, сумятицу: какой в Одессе МУР? Тут, впрочем, 
  по иронии судьбы оказывается, что даже позднейшая вставка Московского уголовного 
  розыска не нарушает исторической правды. Как вспоминал участник белогвардейского 
  движения В. Шульгин в своих мемуарах, в 1919 году «одесская чрезвычайка получила 
  из Москвы 400 абсолютно верных и прекрасно выдрессированных людей». <br>
  <br>
  Интересно также, что в ранних вариантах песни чрезвычайно подробно описывается 
  и сам процесс расправы над предательницей, и даже судьба тех уркаганов, которые 
  ей отомстили. А в «московском» варианте даже описаны похороны Мурки «лягашами»! 
  Канонический текст в конце концов от этого отказался, выиграв в экспрессивности 
  и динамичности сюжета. <br>
  <br>
  В качестве приложения также мною приводится «легальная» «Мурка», из которой 
  выхолощены все воровские реалии; этот вариант песни тиражировался на пластинках, 
  выпущенных в послевоенные годы в Ленинграде. О знаменитом припеве «Мурка, ты 
  мой Мурёночек» см. примечание к «московскому» тексту «Мурки», строфа о похоронах. 
  <br>
