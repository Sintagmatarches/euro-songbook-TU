<strong>МУРКА</strong><br></span>
  <br>
  Прибыла в Одессу банда из Ростова,<br>
  В банде были урки-шулера.<br>
  Банда заправляла темными делами,<br>
  А за ней следили мусора. <br>
  <br>
  Верх держала баба – звали ее Мурка,<br>
