  Мурка окровавлена лежит. <br>
  <br>
 <em>Расшифровка фонограммы Алексея Козлова, альбом «Пионерские блатные 
  2», ТОО «Московские окна ЛТД», 1998.<br></em>
<br>
  <br>
  <br>
        Песня появилась в 1920-е годы в Одессе и долгое время изменялась. В ранних 
        версиях героиню звали Маша или Любка. Первоначальное имя песни, скорее 
        всего - &quot;Любка&quot;. Авторы не установлены. Есть версия, что автором 
        текста (а также автором текста <a href="gopsosmykom.php">&quot;Гоп-со-смыком&quot;</a>) 
        был одесский поэт Яков Ядов - создатель текста <a href="bubliki.php">&quot;Бубликов&quot;</a>, 
        но прямых доказательств нет. См. статьи Владимира Бахтина <a href="piter/a-jadov.php">&quot;Забытый 
        и незабытый Яков Ядов&quot;</a> (&quot;Нева&quot;, 2001, №2) и <a href="piter/a-murkina.php">&quot;Муркина&quot; 
        история&quot;</a> ("Нева", 1997, №4). <br>
  <br>
  В годы Великой Отечественной под &quot;Мурку&quot; ходили в атаку части с лагерным 
  прошлым (в том числе польские части в составе Красной Армии - они формировались 
  в Казахстане из военнопленных кампании 1939 года). Частей &quot;с прошлым&quot; 
  было много - под Ленинградом, например, сражалась &quot;Кулацкая дивизия&quot;, 
  набранная из бывших раскулаченнных. Воевали в штрафбатах и многие уркаганы, 
  хотя &quot;воровской закон&quot; категорически запрещал брать оружие из рук 
  власти: выжившие на полях Второй мировой урки-фронтовики вскоре пали первыми 
  жертвами &quot;сучьей войны&quot;, которая началсь в лагерях между &quot;ворами&quot; 
  старого воровского закона и отступниками от него (&quot;суками&quot;).<br>
  <br>
  <br>
  Иногда песня начинается словами <em><strong>&quot;Прибыла в Одессу банда из 
  Амура&quot; (или &quot;из Амурки&quot;)</strong></em>. Единого мнения, что это 
 за &quot;Амур&quot;, нет. Фима Жиганец (см. ниже) предположил, что так некогда 
  назывался какой-то район Одессы. Другую версию, более правдоподобную, прислал 
  31 августа 2006 года на гостевую сайта a-pesni жж-юзер <a href="http://slavko.livejournal.com" target="_blank">Slavko</a> из Днепропетровска: 
  <br>
  <br>
  <em>&quot;Имеется в виду некогда Екатеринославский, а теперь Днепропетровский 
  &quot;Амур&quot; (Амур-Нижнеднепровский район) - исторический район города, 
  он и сейчас пользуется дурной славой. В 80-х гг. нам рассказывал об этом наш 
  школьный учитель Иван Ефремович - весельчак, художник и знаток фольклора&quot;.</em><br>
  <br>
  В этом случае банда приехала из Екатеринослава - что недалеко от Одессы. Но 
  тогда тоже остается противоречие: Мурку в этом варианте песни убили на Амурке, 
  но как это могло быть, если банда гастролировала в Одессе, и именно в Одессе 
  происходит всё действие? <br>
  <br>
  Еще пара версий (скорее, курьезных) - в статье Александра Макарова <a href="odessa/a-neskchtr.php">&quot;Несколько 
  штрихов о том, как родилась одесская &quot;Мурка&quot;</a>: Амурка или Амур 
  появились как искажение от <em>&quot;Прибыла в Одессу банда из-за МУРа&quot;</em> 
  (московская банда, бежавшая в Одессу от сотрудников Московского уголовного розыска) 
  или просто от <em>&quot;Прибыла в Одессу банда из-за Мурки&quot;</em> (прибыла 
  в Одессу по непонятной причине из-за главной героини песни).<br>
  <br>
