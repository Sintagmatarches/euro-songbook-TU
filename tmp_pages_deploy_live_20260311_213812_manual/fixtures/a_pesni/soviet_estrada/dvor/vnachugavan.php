  В НАШУ ГАВАНЬ ЗАХОДИЛИ КОРАБЛИ <br>
  <br>
  В нашу гавань заходили корабли, <br>
  Большие корабли из океана. <br>
  Матросы веселились, как могли, <br>
  И пили за здоровье атамана. <br>
