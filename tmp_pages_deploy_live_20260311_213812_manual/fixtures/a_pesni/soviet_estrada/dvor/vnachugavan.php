  И Мэри в этот миг его любила. <br>
  <br>
  Со стоном повалился атаман. <br>
  И губы его тихо прошептали: <br>
  «Погиб пират – пусть плачет океан». <br>
  Кровь капала с ножа ковбоя Гарри. <br>
  <br>
  В нашу гавань заходили корабли, <br>
  Большие корабли из океана. <br>
  В таверне веселились моряки, <br>
  И пили на поминках атамана. <br>
