  Далее все совпадает с “каноническим” текстом, кроме концовки: <br>
  <br>
  В нашу гавань заходили корабли, <br>
  Большие корабли из океана. <br>
  В таверне веселились моряки <br>
  И выбирали Гарри атаманом. <br>
  <br>
  Кстати, эта концовка как бы ставит точку в споре о том, кем же все-таки был 
  Гарри - капитаном или атаманом. Многие телезрители горячо настаивали на капитане, 
  поскольку речь идет о моряках, забывая при этом, что они еще и пираты. Комментарии 
  – Ольги Янишевской.<br>
