  Авторы песни не установлены. Появилась она, вероятно, в эпоху НЭПа (1920-е годы). 
  В начале 1990-х песня дала имя знаменитой радио-, а затем телепередаче Эдуарда 
  Успенского и Элеоноры Филиной &quot;В нашу гавань заходили корабли&quot;, которая 
  вызвала ренессанс старого дворового фольклора в России. <br>
  <br>
  Есть близкая песня <a href="devskosoju.php">&quot;Девушка с распущенной косою&quot;</a> 
  - смесь из песен &quot;В нашу гавань заходили корабли&quot;, <a href="devuchkasolen.php">&quot;Девушка 
  с оленьими глазами&quot;</a> и <a href="ivolga.php">&quot;Иволга&quot;</a>, 
  на мелодию последних.<br>
  <br>
  См. также песню <a href="jungabill.php">&quot;Юнга Билл&quot;</a> - в одной 
  из ее версий юнга Билл, проиграв сражение за Мери, меняет имя и становится ковбоем 
  Гарри. Тогда песня &quot;В нашу гавань заходили корабли&quot; становится продолжением 
  этой истории - залечив раны, Билл-Гарри возвращается в ту же самую таверну, 
  во второй раз сражается за Мери и выигрывает. Есть даже &quot;сводный&quot; 
  текст из двух песен:<br>
