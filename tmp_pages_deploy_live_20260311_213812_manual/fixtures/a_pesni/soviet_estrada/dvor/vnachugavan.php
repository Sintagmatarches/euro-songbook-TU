  почте, были замечания по тексту, связанные с теми вариантами, которые телезрители 
  считали единственно правильными. Но вот один вариант, который как бы синтезировал 
  две песни, нам представляется интересным, поскольку более логичен и выстраивается 
  в целостный сюжет. <br>
