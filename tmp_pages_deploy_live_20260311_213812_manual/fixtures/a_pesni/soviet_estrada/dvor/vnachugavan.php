  В нашу гавань заходили корабли - <br>
  <br>
  Далее следует текст, приведенный вначале, но в конце имеется добавление, свидетельствующее 
  о том, что Гарри был действительно ковбоем: <br>
  <br>
  А конь его привязан у ворот, <br>
  Хозяина недолго дожидался: <br>
  Вот Гарри заскочил к нему в седло <br>
  И в степь свою далекую умчался. <br>
