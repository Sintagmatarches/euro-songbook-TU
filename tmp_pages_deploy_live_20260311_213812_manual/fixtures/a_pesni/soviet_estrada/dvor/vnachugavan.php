  Глаза его как молния сверкали. <br>
  Наездник был красивый сам собой, <br>
  Его все знали как ковбоя Гарри. <br>
  <br>
  «Мэри, вот вернулся, Гарри твой!» <br>
  «Нет братцы, он не наш, не с океана! <br>
  Я, Гарри, рассчитаюся с тобой!» — <br>
  Раздался пьяный голос атамана. <br>
  <br>
  И в воздухе сверкнули два ножа. <br>
  Матросы затаили все дыханье. <br>
  Все знали атамана как вождя <br>
