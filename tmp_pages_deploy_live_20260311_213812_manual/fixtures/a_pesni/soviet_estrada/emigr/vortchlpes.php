Слова и музыка Александра Вертинского<br>
<br>
Тяжело таким, как я, отсталым папам:<br>
Подрастают дочки и сынки…<br>
И уже нас прибирают к лапам<br>
Эти «критики» и «знатоки»!<br>
<br>
Вот, допустим, выскажешь сужденье<br>
(Может, ты всю жизнь над ним потел!) –<br>
Им смешно. У них другое мненье…<br>
«Ты, отец, ужасно устарел!»<br>
<br>
Виноват! Я – в ногу! А одышка?..<br>
Это, так сказать, уже не в счет!<br>
Не могу ж я, черт возьми, вприпрыжку<br>
Забегать на двести лет вперед!..<br>
<br>
Ну, конечно, спорить бесполезно,<br>
Отвечать им тоже ни к чему…<br>
Но упрямо, кротко и любезно<br>
Можно научить их кой-чему!<br>
<br>
Научить хотя б не зазнаваться<br>
И своих отцов не презирать,<br>
Как-то с нашим возрастом считаться,<br>
Как-то все же старших уважать!<br>
<br>
Их послушать – так они «большие»,<br>
Могут целым миром управлять!<br>
Впрочем, замыслы у них такие,<br>
Что, конечно, трудно возражать!<br>
<br>
Ну, и надо в общем соглашаться…<br>
Отходить в сторонку… и молчать,<br>
Как-то с этим возрастом считаться,<br>
Как-то этих младших уважать!<br>
<br>
И боюсь я, что придется папам<br>
Уступить насиженный престол,<br>
Все отдать бесцеремонным лапам<br>
И пойти учиться… в комсомол!<br>
