  <br>
  Между маем 1825 и февралем 1826<br>
  <br>
  <em>«Стихотворения барона Дельвига», СПБ. 1829 г. - под загл. &quot;Русская песня&quot;<br>
  <br>
  Русские песни и романсы / Вступ. статья и сост. В. Гусева.  М.: Худож. лит., 
  1989.  (Классики и современники. Поэтич. б-ка), под загл. &quot;Русская песня&quot;. </em><br>
 <br>
  <br>
  У Дельвига озаглавлено «Русская песня», что, впрочем, не заглавие, 
  а обозначение жанра. В авторском тексте нет повторов &quot;Соловей мой, соловей, 
  / Голосистый соловей!&quot; после каждой строфы, они введены для романса. Первый 
  исполнитель - московский оперный певец Петр Булахов, отец композитра Петра Булахова 
  (<a href="../bulahov/gorigori.php">&quot;Гори, гори, моя звезда...&quot;</a>, <a href="../../popular20/kolokol.htm">&quot;Колокольчики&quot;</a> 
  и др.) и певца и композитора Павла Булахова (<a href="../../popular20/3mtchitsa.htm">&quot;Тройка 
  мчится, тройка скачет...&quot;</a>). Есть вариации на тему алябьевского 
  &quot;Соловья&quot; Михаила Глинки, Александра Гурилева, Франца Листа - для фортепиано, 
  Михаила Высотского - для гитары, Александра Вивьена - для скрипки.<br>
  <br>
  <br>
  Из предисловия Виктора Калугина к сб.: Антология русской песни. М.:  
  Эксмо, 2005:<br>
  <br>
  <em>«Соловей» впервые прозвучал в Большом театре 7 января 1827 года в исполнении 
  кумира московских меломанов Петра Булахова (Сергея Лемешева того времени) и 
  вошел в репертуар самых выдающихся русских и зарубежных исполнителей. Современники 
  Пушкина слышали «Соловья» в исполнении знаменитой Прасковьи Бартеневой, немецким 
  «оперным дивом» Генриэттой Зонтаг (ее даже называли «соловьем»), а в 40—50-е 
  годы «Соловей» стал коронным номером Полины Виардо, включавшей его во все свои 
  концертные программы и в сцену урока пения в «Севильском цирюльнике», что вызывало 
  шквал аплодисментов.</em><br>
