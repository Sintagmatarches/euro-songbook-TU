	<h1>АХ ТЫ БЕДНЫЙ, 
ТЫ БЕДНЫЙ СОЛДАТИК </h1>
 <br>
 Ах ты бедный, ты бедный 
  солдатик,<br>
  Ты на службу попал с юных лет,<br>
  Нелегка брат, солдатская служба,<br>
  Много вынес ты горя и бед.<br>
 <br>Тебя рано на службу призвали,<br>
  Ты любимчик был сын у отца,<br>
  Служить ты пошел со слезами,<br>
  Проклиная всю жизнь до конца.<br>
 <br>Когда на службу тебя провожали,<br>
  Горько плакала старая мать.<br>
  С молодою женою прощался,<br>
  Не мог слова, бедняга, сказать.<br>
 <br>Погнали тебя в край суровый,<br>
  Далеко от родимых полей,<br>
  Ты служишь в бригаде стрелковой,<br>
  На границе турецких земель.<br>
 <br>Там гоняют тебя, как собаку,<br>
  От зари и до поздних часов.<br>
  Всю ты службу прослужишь, бедняга,<br>
  Не услышишь ты ласковых слов.<br>
 <br>Будет бить тебя взводный 
  по морде,<br>
  Фельдфебель наряды давать,<br>
  Через них попадешь ты в штрафную,<br>
  Тюремную жизнь испытать.<br>
 <br>Тюремный порог переступишь,<br>
  Будет голод тебя там томить.<br>
  А потом всё пройдет чередою,<br>
  Ты будешь, как прочие, жить.<br>
 <br>Будешь есть ты горячую пищу,<br>
  Не будешь босой ты ходить.<br>
  Но в награду за это начальство<br>
  Жадно кровь из тебя будет пить.<br>
 <br>И в тюрьме ты чахотку получишь,<br>
  Будешь в койке больничной лежать,<br>
  В слезах втихомолку ночами<br>
  Будешь родную мать вспоминать.<br>
 <br>Ты не плачь понапрасну ночами,<br>
  Горючие слезы не лей,<br>
  А возьми ты, солдатик, винтовку<br>
  И все начальство свое перебей!<br>
 <br>
 <span class='i'>В нашу гавань заходили 
  корабли. Вып. 2. М.: Стрекоза, 2000.</span><br>
 <br><br>
  Песня о тяготах казарменной жизни с оптимистическим освободительным финалом. Судя по поэтическому размеру и содержанию, создана между революцией 1905 г. и Первой мировой войной - скорее всего, на грани  1900-1910-х гг. Основной прообраз песни -  жестокий романс конца 1900-х гг. "Бедная швейка" (Ах ты бедная, бедная швейка...), на основе стихотворения "Умирающая швейка" Ивана Сурикова (опубл. в 1875).<br>

