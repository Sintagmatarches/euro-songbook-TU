	<h1>ПЕСНЯ</h1>
	<br>
<em>к русским воинам, написанная отставным<br>
Фанагорийского гренадерского полка солдатом,<br>
Никанором Остафьевым. Июль, 1812 г. </em><br>
<br>
 Братцы! Грудью 
  послужите,<br>
  Гряньте бодро на врага<br>
  И вселенной докажите,<br>
  Сколько Русь вам дорога!<br>
<br>Посмотрите, подступает<br>
  К вам соломенный народ;<br>
  Бонапарте выпускает<br>
  Разных наций хилый сброд.<br>
  Не в одной они все вере,<br>
  С принужденьем все идут:<br>
  При чувствительной потере<br>
  На него же нападут.<br>
<br>Братцы! Грудью 
  послужите,<br>
  Гряньте бодро на врага<br>
  И вселенной докажите,<br>
  Сколько Русь вам дорога!<br>
<br>Всем, наверно, 
  дал он слово,<br>
  Что далеко к нам зайдет;<br>
  Знает, дома нездорово,<br>
  Дома также пропадет.<br>
  Мыслит: коль пришла невзгода,<br>
  Должно славу потерять,<br>
  Так от русского народа<br>
  Мне и смерть честней принять.<br>
<br>Братцы! Грудью 
  послужите,<br>
  Гряньте бодро на врага<br>
  И вселенной докажите,<br>
  Сколько Русь вам дорога!<br>
<br>Вся Европа ожидает<br>
  Сей погибели его;<br>
  Бонапарта почитает<br>
  За злодея своего.<br>
  Ах, когда слух разнесется,<br>
  Что от нас сей враг исчез,<br>
  Слава русских вознесется<br>
  До превыспренних небес!<br>
