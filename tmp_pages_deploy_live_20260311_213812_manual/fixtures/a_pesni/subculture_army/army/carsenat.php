<h1>&lt;ЦАРЯ ТРЕБУЮТ В СЕНАТ&gt;<br>
<br>
СОБИРАЙТЕСЬ, МЕЛКА ЧЕРНЯДЬ...</h1>
<br>	
Собирайтесь, мелка чернядь,<br>
  Собирайся на совет. <br>
  Время тяжкое приходит — <br>
  Ладит турка воевать. <br>
  С англичанином они скумились, <br>
  Не могли России взять... <br>
  Не в показанное время, <br>
  Не в указные часы, <br>
  Не в указные часы, <br>
  Да во сам<strong>у</strong>ю во полночь, <br>
  Во сам<strong>у</strong>ю во полночь <br>
  Да царя требуют во сенот. <br>
  Царь недолго сподоблялся, <br>
  На ямских он отправлялся, <br>
  Родну братьцу говорил: <br>
  «Чтобы за мной погоней был». <br>
  Брат недолго сподоблялся, <br>
  На ямских он отправлялся, <br>
  Ко сеноту подъезжал, <br>
  Да часовых-от переспрошал:<br>
  «Господа вы часовые, <br>
  Часовые страждовы! <br>
  Не видали ль здесь, ребята, <br>
  Офицера со вестям —<br>
  Да государя со кистям?» <br>
  Друг на друга поглядели<br>
  И сказали: «Не видал». <br>
  Один солдатичок-провор, <br>
  Левым глазиком повел. <br>
  Никого князь не спрошался, <br>
  Часовых всех прирубил, <br>
  До сеноту доступал. <br>
  Трои двери изломал, <br>
  Он на четвёрты отворял –<br>
  На коленях брат стоял. <br>
  Перед ним стоит полковник — <br>
  Сенаторская судья — <br>
  И держит саблю на весу: <br>
  «Что царю голову снесу». <br>
  Царь да брата увидал, <br>
  На резвы ноги ставал. <br>
  «Те спасибо, братец милой, <br>
  Те спасибо, брат родной, <br>
  Поманил бы час-минуту, <br>
  Скоро не было меня. <br>
  Скоро не было меня, <br>
  Срубили голов у меня! <br>
  Нам не дороги сеноты, <br>
  Сенат<strong>о</strong>рские судьи. <br>
  Из сеноту мы пойдем — <br>
  Сенаторов всех прибьем. <br>
  Из сенотов вон пойдем – <br>
  Все сеноты все сожжем!» <br>
