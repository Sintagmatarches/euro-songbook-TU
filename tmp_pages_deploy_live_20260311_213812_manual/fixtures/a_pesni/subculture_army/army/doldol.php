	<h1>ДОЛИНА, ДОЛИНА, ДА ТЫ ЗЕЛЕНАЯ!..</h1> 
	<br>
	Долина, долина, да ты зеленая! 
  <br>
  По тебе, долина, — дорожка широкая, <br>
  Дорожка широкая, реченька быстрая, <br>
  Реченька быстрая, круты бережочки. <br>
  Как на крутеньком бережочке, <br>
  На нем желтые песочки; <br>
  На желтеньких на песочках <br>
  Стоят три садочка. <br>
  Как во первом во садочке <br>
  Соловьюшек свищет: <br>
  А во втором во садочке <br>
  Кукушка кукует; <br>
  А во третьем во садочке <br>
  Мать с сыном горюет. <br>
  Мать с сыном гуляла, <br>
  Мать сына пытала: <br>
  «Скажи, скажи, мой сыночек, <br>
  Кто из трех милее: <br>
  Жена, теща иль мать родная?» <br>
  — «Жена мне — для совета, <br>
  Теща — для привета; <br>
  А милее, лучше в свете <br>
  Нету матушки родимой: <br>
  Она меня спородила, <br>
  Три ночки не спала, <br>
  Три ночки не спала, <br>
  Три ночки не спала, <br>
  Плакала-рыдала, <br>
  Вскормила-вспоила, <br>
  На конь посадила, <br>
  На конь посадила, <br>
  В службу проводила, <br>
  В службу проводила, <br>
  Речи говорила: <br>
  Служи, служи, мой сыночек, <br>
  Царю ты вернее!» <br>
  <span class='i'><br>
  Русские народные 
