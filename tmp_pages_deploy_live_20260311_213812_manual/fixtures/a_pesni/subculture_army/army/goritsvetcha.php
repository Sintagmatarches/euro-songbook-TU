  СВЕЧА, В ВАГОНЕ ТИХО</h1>
  Солдатская версия<br>
  <br>
  Горит свеча, в вагоне тихо, <br>
  Солдаты все, свернувшись, спят,<br>
  Поезд наш несется лихо,<br>
  Только слышно так-тик-так;<br>
  <br>
  Один солдатик изнуренный, <br>
  Склонил он голову на грудь, <br>
  В тоске по родине далекой <br>
  Никак не может он уснуть.<br>
  <br>
  Расставшись со своей семьею,<br>
  Уже три года прослужил,<br>
  Утерял здоровье, силу,<br>
  А что за это получил?<br>
  <br>
  Проклятье мирного народа<br>
  За то, что он теперь солдат,<br>
  Но кто солдатик был до службы?<br>
  Он был ваш сын, он был ваш брат.<br>
  <br>
  «Эх ты, мать моя родная, <br>
  Зачем на свет породила?<br>
  Несчастной долей наделила, <br>
  Шинель мне серую дала?<br>
  <br>
  Шинель мне плечи отдавила, <br>
  Шинель свободу отняла, <br>
  Шинель меня переродила, <br>
  Навек солдатом назвала.<br>
  <br>
  Эх, Боже мой, что дальше будет,<br>
  Когда свободу заслужу?<br>
  Когда пойду я в бой кровавый,<br>
  За правду голову сложу?»<br>
