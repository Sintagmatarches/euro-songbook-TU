  (1785) принадлежит к числу значительных одических произведений на военную тему. 
  Другие жанры в поэтическом наследии Карабанова занимают весьма скромное место, 
  но знаменитая пастораль Петра Чайковского из оперы "Пиковая дама" "Мой миленький 
  дружок, любезный пастушок" создана через столетие на его стихи, и одна из самых 
  популярных солдатских песен тоже принадлежит ему - одописцу Карабанову. <br>
