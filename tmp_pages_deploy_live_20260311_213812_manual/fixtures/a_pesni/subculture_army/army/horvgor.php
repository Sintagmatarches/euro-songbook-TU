	<h1>ХОРОШО В ГОРЯЩЕМ ТАНКЕ</h1>
Хорошо в горящем танке<br>
Прокатиться при луне<br>
Или посидеть в землянке<br>
Под обстрелом, в тишине.<br>
<br>
Я люблю стрелять из пушки<br>
По окрестным городкам<br>
И, сминая деревушки,<br>
Прокатиться по садкам.<br>
<br>
Хорошо в тиши природы,<br>
В поднебесной синеве<br>
Возле Статуи Свободы<br>
Поваляться на траве. (вариант: Помочиться на траве)<br>
<br>
Перед нами снова речка,<br>
А за нею снова лес.<br>
Ты не плачь, мое сердечко!<br>
Нас ведет товарищ Гречко,<br>
Член ЦК КПСС!<br>
<br>
А когда в зените славы<br>
Я окончу свой поход,<br>
Напиши мне под Оттаву,<br>
Как наш мальчик спит и пьет. (Вариант: С кем наш мальчик спит, что пьет)<br>
