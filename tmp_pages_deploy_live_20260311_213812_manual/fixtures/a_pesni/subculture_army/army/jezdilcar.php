	<h1>ЕЗДИЛ РУССКИЙ БЕЛЫЙ ЦАРЬ </h1>
  <br>
  Ездил русский Белый Царь, <br>
  Православный государь <br>
  Из своей земли далеко <br>
  Злобу поражать! <br>
  Злобу поражать!<sup>1</sup><br>
  Грозно он карал врагов, <br>
  Много побрал городов: <br>
  От Москвы и до Парижа <br>
  Лавры пожинал, <br>
  Лавры пожинал. <br>
  Крови там лились моря, <br>
  Смерть летала вкруг царя: <br>
  Посреди военной бури <br>
  Бог его хранил, <br>
  Бог его хранил! <br>
  Он за зло платил добром, <br>
  Воскресил Бурбонов дом, <br>
  Имя русское прославил, <br>
  Мир вселенной дал, <br>
  Мир вселенной дал. <br>
  Где ты, светлый царь царей, <br>
  Ангел мира, друг людей? <br>
  Где ты, солнце наше красно, <br>
  Где ты, где теперь? <br>
  Где ты, где теперь? <br>
  Или в царстве ты лилей? <br>
  Иль средь радостных морей? <br>
  Или, света став любовью, <br>
  Позабыл детей? <br>
  Позабыл детей? <br>
  Полно радовать чужих, <br>
  Нам завидно счастье их; <br>
  Возвратись из стран далеких <br>
  На родимый край! <br>
  На родимый край! <br>
  Без тебя веселья нет, <br>
  Опостыл нам белый свет, <br>
  Грустно без отца родного, - <br>
  Возвратись скорей, <br>
  Возвратись скорей. <br>
  Ждет тебя царица-мать, <br>
  Жаждет сына лобызать;<br>
  Ждет прекрасная царица, <br>
  Ждет святая Русь, <br>
  Ждет святая Русь. <br>
  О, зефиры на крылах, <br>
  Или реки на волнах, <br>
  Принесите нам скорее <br>
  Нашего царя! <br>
  Нашего царя! <br>
