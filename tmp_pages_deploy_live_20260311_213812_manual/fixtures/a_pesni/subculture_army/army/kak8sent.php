  ВОСЬМОГО СЕНТЯБРЯ... </h1>
  <br>
  Как восьмого сентября <br>
  Мы за веру и царя <br>
  От француз ушли. <br>
  И так храбро отступали, <br>
  Что всех раненых бросали <br>
  Умирать в степи. <br>
  Меньшик, умный адмирал, <br>
  Суденушки потоплял <br>
  В море-пучине. <br>
  Молвил: «Счастия желаю», <br>
  Сам ушел к Бакчисараю, <br>
  Ну вас всех &lt;...&gt; <br>
  Сент-Арно позакопался, <br>
  Он учтиво обращался — <br>
  Сзаду обошел. <br>
  Кабы нам да в этот вторник <br>
  Не помог святой угодник, <br>
  Всех бы нас забрал. <br>
  С моря, с суши обложили, <br>
  Севастополь наш громили <br>
  Из больших маркел. <br>
  Духовенство всё молило, <br>
  Чтоб француза потопило, — <br>
  Бурю бог послал. <br>
  Была сиверка большая, <br>
  Но француз, не унывая, <br>
  На море стоял. <br>
  И князья к нам приезжали, <br>
  Да француза не спужали, - <br>
  Всё палит с маркел. <br>
  Штуцеров мы поджидали, <br>
  Да гвардейцы их забрали, -<br>
  Видно, им нужней, <br>
  Из-под града Кишинева <br>
  Ждали войска мы большого, -<br>
  Войско подошло. <br>
  Даненбергу поручили, <br>
  Его очинно просили <br>
  Войска не жалеть. <br>
  Павлов, Соймонов ходили, <br>
  Круты горы восходили, <br>
  Вместе не сошлись. <br>
  А Липранди хоть видал, <br>
  Как француз одолевал, — <br>
  Руку не подал. <br>
  Тысяч десять положили – <br>
  От царя не заслужили <br>
  Милости большой. <br>
  Князь изволил рассердиться: <br>
  Наш солдат-де не годится, <br>
  &lt;...&gt; показал. <br>
  Из сражения большого <br>
  Вышло только два героя, <br>
  Их высочества. <br>
  Им повесили Егорья, <br>
  Повезли назад на взморье, <br>
  В Питер напоказ. <br>
  Зимой вылазки чинили, <br>
  Много войска положили, — <br>
  Всё из-за туров. <br>
  Меньшик просит подкрепленья, <br>
  Царь ему во утешенье <br>
  Сакена послал. <br>
  Меньшик, умный адмирал, <br>
  Царю прямо отписал: <br>
  «Батюшка, наш царь, <br>
  Ерофеич твой не крепок, <br>
  От твоих же малолеток <br>
  Толку ничего». <br>
  Царь на Меньшика серчал, <br>
  И в ту пору захворал <br>
  На одном смотру. <br>
  И отправился на небо, <br>
  Видно, в нем была потреба, — <br>
  И давно пора! <br>
  А когда же умирал <br>
  Сыну крепко наказал: <br>
  «Ты теперь смотри!» <br>
  Сын же к Меньшику писал: <br>
  «Ты, мой милый адмирал, <br>
  К черту, брат, тебя. <br>
  Я пошлю туда другого, <br>
  Того князя Горчакова, <br>
  К турку что ходил; <br>
  Много войск ему не надо, <br>
  Будет пусть ему награда <br>
  Красные штаны». <br>
  Посылали тут Хрулева <br>
  Выгнать турку из Козлова, —<br>
  Наша не взяла. <br>
  И вот ждем и поджидаем, <br>
  Севастополь защищаем, —<br>
  Скоро ль отойдет?<br>
