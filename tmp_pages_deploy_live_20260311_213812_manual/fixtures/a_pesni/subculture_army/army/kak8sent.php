  Севастопольские песни &quot;Как восьмого сентября&quot; и <a href="kak4tchisla.php">"Как четвертого числа"</a> приписывались современниками Льву Толстому. Сам Лев Толстой в ответ на запрос М.Н. Милошевич, которая готовила к публикации воспоминания своего отца, участника обороны Севастополя, в двух письмах (от 19 февраля и от 18 мая 1906 г.) сообщил, что участвовал в сочинении &quot;Как восьмого сентября&quot; и сочинил &quot;Как четвертого числа&quot;, причем у обеих песен не было записанного авторского текста, и бытовали они устно. Тексты писем см.: <a href="../books/sreznevskij1925.pdf" target="_blank"><em>Срезневский В. К вопросу о принадлежности Л.Н. Толстому севастопольских песен</em></a><em> // Литературная мысль: [Альманах]. Вып. III. Л.: Мысль, 1925. С. 387-392.</em><br>
