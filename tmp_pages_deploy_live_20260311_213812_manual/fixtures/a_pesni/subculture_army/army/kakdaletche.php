	<h1>КАК ДАЛЕЧЕ-ДАЛЕЧЕНЬКО, ВО ЧИСТОМ ПОЛЕ...</h1>
	<br>
	Как далече-далеченько, во 
  чистом поле, <br>
  Еще того подале — во раздольице, <br>
  Как не белая березынька к земле клонится, <br>
  Не бумажные листочки раздуваются, <br>
  Не шелковая ковыль-травынька расстилается – <br>
  Еще стелется-расстилается полынь горькая. <br>
  Ох и нет тебя горчее во чистом поле, <br>
  А еще того горчее служба царская! <br>
  Что ни день-то, ни ночь нам, солдатушкам, угомону нет: <br>
  Темна ноченька приходит — нам, солдатушкам, на карауле быть; <br>
  Бел денечек наступает — нам, солдатушкам, во строю стоять; <br>
  Во строю стоять нам, солдатушкам, — по ружью держать; <br>
  Пристоялися резвы ноженьки ко сырой земле, <br>
  Придержалися белы рученьки к строеву ружью, <br>
  Пригляделися наши глазыньки на часты звезды.<br>
  <span class='i'><br>
  Русские народные 
  песни. / Сост. и вводн. тексты В.В. Варгановой. М.: Правда, 1988. Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания».</span><br>
<br>
<br>
Солдатская песня XVIII или XIX в.<br>

