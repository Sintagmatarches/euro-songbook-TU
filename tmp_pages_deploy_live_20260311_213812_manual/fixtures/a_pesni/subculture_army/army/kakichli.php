	<h1>КАК И ШЛИ, 
ПРОШЛИ СОЛДАТЫ МОЛОДЫЕ... </h1><br>Как и шли, прошли солдаты молодые <br>
  Да за ними идут матушки родные, <br>
  Во слезах пути-дороженьки не видят;<br>
  Как возговорят солдаты молодые: <br>
  «Ох вы, матушки родные, да родные, <br>
  Не наполнить вам синя моря слезами, <br>
  Не исходить-то вам сырой земли за нами». <br><br>
  <span class='i'>Якушкин П.Я.  
  Сочинения. Народные стихи и песни. СПб. 1884. № 28. <br>
  <br>
  Русские песни / Сост. проф. Ив. Н. Розанов. М.: Госиздат, 
  1952.</span><br>
  <br>
  <br>
  Песня XVIII или XIX в.<br>
  <br>
  <br>
  <span class='bold'>ВАРИАНТ<br>
  <br>
  Вдоль по Питерской широкой по дорожке ...</span><br>
  <br>Вдоль по Питерской широкой по дорожке <br>
	  Тут и шли-прошли солдаты молодые, <br>
	  А за ними идут матушки родные. <br>
	  «Сколь вам, матушки, и гнаться — расставаться! <br>
	  Всю Россеюшку за нами не испройдешь, <br>
	  Сыру землю всю слезами не намочишь!» <br><br>
	  <span class='i'>Русские народные песни / Сост. и вводн. тексты В.В. Варгановой. 
      М.: Правда, 1988. Раздел «Песни солдат и о солдатах. Рекрутские причитания».</span><br>

