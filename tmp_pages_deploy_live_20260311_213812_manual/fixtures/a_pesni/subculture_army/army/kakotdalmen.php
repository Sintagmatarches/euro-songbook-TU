	<h1>КАК ОТДАЛ МЕНЯ БАТЮШКА ЗАМУЖ...</h1>
	<br>
	Как отдал меня батюшка замуж, 
  <br>
  Он далече-далече, в иной город, <br>
  Что в иной город замуж за солдата, <br>
  Их и горькая солдатская женитьба! <br>
  Он ни ноченьки дома не ночует, <br>
  Хоть ночует, да всю ночку протоскует, <br>
  Во косящато окошечко просмотрит, <br>
  Что не идут ли, не едут ли солдаты; <br>
  Вот и старые солдаты едут-скачут, <br>
  Молодые новобранцы слезно плачут, <br>
  А их старые солдаты унимают: <br>
  «Вы не плачьте, не тужите, новобранцы!» <br>
  — «Ах и как же нам, солдатушкам, не плакать?<br>
  Наши новые домы запустели, <br>
  Молодые наши жены завдовели, <br>
  Наши малые дети осиротели!» <br> <br>
  <span class='i'>Русские народные 
  песни. / Сост. и вводн. тексты В.В. Варгановой.  М.: Правда, 1988. Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания».</span><br>
<br>
	<br>
	Песня, вероятно, XIX в. о тяжелой судьбе солдатки. Отставной солдат до начала XX в. считался плохим женихом, особенно  среди крестьян, так как за время службы терял связи в своей прежней среде, тогда как его сверстники, пока тот служил, работали и повышали свой социальный статус. В реальности солдат также и приобретал новые навыки и связи, потому ему обычно выгоднее было не возвращаться к прежней жизни, а искать счастья в другом месте. Бывших гвардейцев, например, охотно брали на работу в столичные дворянские дома. <br>
	
