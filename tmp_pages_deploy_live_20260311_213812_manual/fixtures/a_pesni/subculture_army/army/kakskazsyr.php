	<h1>КАК СКАЗАЛИ СЫРОМУ ДУБОЧКУ...</h1>
	<br>
	Как сказали сырому дубочку: 
  <br>
  «Утром рано мороз будет». <br>
  — «Я морозу, дубок, не боюся, <br>
  Сейчас я развернуся». <br>
  Как сказали молодому солдату: <br>
  «Утром рано поход будет». <br>
  — «Я походу, солдат, не боюся, <br>
  Я сейчас пойду соберуся». <br>
  Собирался солдат, снаряжался, <br>
  И со всеми солдат прощался. <br>
  «Вы прощайте, добрые люди, <br>
  Может, с кем я поругался». <br>
  Поругался солдат, побранился, <br>
  С дороженьки воротился. <br>
  Зарастайте мои все тропы-дороги <br>
  Травой-муравою, <br>
  Листом-лопухами, <br>
  Пестрыми цветами. <br>
  <span class='i'><br>
  Русские народные 
