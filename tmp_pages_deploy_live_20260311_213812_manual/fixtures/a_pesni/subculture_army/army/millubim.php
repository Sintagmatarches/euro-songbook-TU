Милая, любимая, далёкая, <br>
Вспоминай и ты меня! <br>
<br>
На лицо снежинки опускаются. <br>
На ресницах тают, как слеза. <br>
Сквозь пургу мне мило улыбаются <br>
Девичьи любимые глаза. <br>
<br>
А солдата ветры бьют жестокие, <br>
Ничего — он ветру только рад! <br>
Милая, любимая, далёкая, <br>
Должен всё снести солдат. <br>
<br>
Нас судьба с тобой одним обидела: <br>
Далеко ты от меня живёшь. <br>
Мы с тобой давно уже не виделись, <br>
Долго не встречались — ну так что ж! <br>
<br>
Ведь любовь не меряется сроками, <br>
Если чувством связаны сердца. <br>
Милая, любимая, далёкая, <br>
Верная мне до конца! <br>
