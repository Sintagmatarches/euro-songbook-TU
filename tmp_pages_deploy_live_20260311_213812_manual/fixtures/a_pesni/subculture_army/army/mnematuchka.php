	<h1>МНЕ, МАТУШКА, ТОШНЕХОНЬКО...</h1>
<br>
	Мне, матушка, тошнехонько, <br>
  Государыня, грустнехонько, <br>
  Мне пить и есть не хочется, <br>
  Мне гульба, прохлад на ум нейдут: <br>
  Мне милый друг с ума нейдет. <br>
  Сильна-крепка я разумом, <br>
  Но я пала-перепалася: <br>
  С милым другом давно виделась, <br>
  Через речку ему поклонилася, <br>
  Через поле слово молвила. <br>
  Все полки домой идут, <br>
  Моего дружка коня ведут, <br>
  Буланого, приубраного, <br>
  Сивогривого, белокопытного; <br>
  В тороках везут его рубашечку, <br>
  Рубашечку боевую, кровавую. <br>
  Умирал мой друг, приказывал: <br>
  «Вы братцы мои, товарищи,<br>
  Может, бог велит вам домой прийти — <br>
  Поклонитесь вы моему батюшке, <br>
  Сударыне родной матушке; <br>
  Милой жене слово молвите, <br>
  Чтобы вымыла мою рубашечку, <br>
  Не на речке, не в колодезе, <br>
  А вымыла горючей своей слезой; <br>
  Чтоб высушила мою рубашечку <br>
  Не на ветре, не на солнышке, <br>
  А высушила на белой своей груди».<br>
