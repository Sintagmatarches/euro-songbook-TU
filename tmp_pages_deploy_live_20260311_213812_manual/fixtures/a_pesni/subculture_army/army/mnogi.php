	<h1>МНОГИ ЛЕТА, МНОГИ ЛЕТА</h1>
	
  Неизвестный автор<br>
  <br>
  «Многи лета, многи лета <br>
  Православный Русский Царь!..» <br>
  Дружно, громко песня эта <br>
  Пелась прадедами встарь. <br>
  <br>
  Дружно, громко песню эту <br>
  И теперь вся Русь твердит; <br>
  С ней по целому полсвету <br>
  Имя царское гремит. <br>
  <br>
  Ей повсюду, отвечая, <br>
  Мчится русское «ура» <br>
  От Кавказа до Алтая, <br>
  От Амура до Днепра. <br>
  <br>
  С ней во дни Петровы шведу <br>
  Русский путь загородил <br>
  И за Нарвскую победу <br>
  Днем Полтавы отплатил. <br>
  <br>
  С ней во дни Екатерины <br>
  Славен стал наш русский штык, <br>
  И Кагульские дружины, <br>
  И Суворовский Рымник. <br>
  <br>
  С нею грозно запылала <br>
  Венценосная Москва, <br>
  И небесной карой пала <br>
  На врагов ее глава. <br>
  <br>
  В наши дни перешагнула<br>
  С нею рать Балканов грань,<br>
  Потрясла врата Стамбула,<br>
  Повалила Эривань.<br>
  <br>
  Прогреми ж до граней света<br>
  И по всем сердцам ударь,<br>
  Наша песня: «Многи лета,<br>
  Православный Русский Царь!»<br>
  <br>
  <span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006.</span><br>
