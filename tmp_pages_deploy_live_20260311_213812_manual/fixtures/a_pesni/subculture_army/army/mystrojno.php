<h1>МЫ СТОЙНО СТОЯЛИ НА ШКАНЦАХ </h1>
<br>
Мы стройно стояли на шканцах,<br>
	Порывы свистали в снастях.<br>
Обернут был в старый он парус,<br>
	С тяжелым ядром на ногах.<br>
<br>
	Мы в церковь его не носили,<br>
И поп там над ним не читал.<br>
	Катилась слеза за слезою,<br>
	У борта он, бедный, лежал.<br>
<br>
	Мы тихо его хоронили<br>
	В холодных и бурных волнах.<br>
	За борт его тело спустили<br>
	С тяжелою думой в сердцах.<br>
<br>
	Прощай же, братишка на службе,<br>
	Просто нам за этот наряд...<br>
Быть может, сейчас за тобою<br>
	Погибнет и весь наш фрегат...<br>
	<br>
	Но нас не схоронят так грустно,<br>
К ногам не привяжут ядро,<br>
	А просто - погибнем в пучине,<br>
	Куда опустили его...<br>
