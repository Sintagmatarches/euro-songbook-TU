	<h1>НА ЗАРЕ БЫЛО, БРАТЦЫ, НА ЗОРЮШКЕ...</h1>
<br>
	На заре было, братцы, на зорюшке, <br>
  Да солнышко садится. <br>
  Под удалым удал-добрым молодцем <br>
  Стал конь становиться. <br>
  Под удалым да и удал-добрым молодцем <br>
  Стал конь становиться. <br>
  Стал сбиваться удал-добрый молодец <br>
  Да и с пути, с дорожки, <br>
  Стал сбиваться удал-добрый молодец <br>
  Да и с пути, с дорожки. <br>
  Подъезжает удал-добрый молодец <br>
  К кусту, кусточку, <br>
  Подъезжает удал-добрый молодец <br>
  К кусту, кусточку. <br>
  «Здравствуй, здравствуй, родимый кусточек, <br>
  Горький полыночек! <br>
  Позволь, позволь, родимый кусточек, <br>
  Да и ночку ж ночевать!» <br>
  Позволь, родимый кусточек, <br>
  Да и ночку ж ночевати!» <br>
  — «Ночуй, ночуй, удал-добрый молодец, <br>
  Бог с тобою! <br>
  Постель тебе, удал-добрый молодец, <br>
  Да всё травка ковылка. <br>
  Постель тебе, удал-добрый молодец, <br>
  Да всё травка ковылка, <br>
  А в головушки тебе, удал-добрый молодец, <br>
  Да высокая могилка, <br>
  А в головушки тебе, удал-добрый молодец, <br>
  Высокая могилка, <br>
  Одеяльце тебе, удал-добрый молодец, <br>
  Всё темная ночка. <br>
  Э-э-эх, часовые тебе, удал-добрый молодец, <br>
  Да всё лютые звери». <br>
