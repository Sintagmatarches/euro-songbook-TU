    <h1>НЕ КУКУШЕЧКА ВО СЫРОМ БОРУ КУКОВАЛА...</h1>
<br>
	Не кукушечка во сыром бору 
  куковала, <br>
  Не соловушка в зеленом саду громко свищет — <br>
  Добрый молодец, во неволюшке сидя, плачет, <br>
  Обливается горючими слезами. <br>
  Как берут меня, доброго молодца, в неволю, <br>
  Уж как вяжут добру молодцу белы руки, <br>
  Что куют-куют добру молодцу скоры ноги, <br>
  Везут-везут добра молодца во город, <br>
  Отдают там доброго молодца в царску службу, <br>
  Что во ту ли царску службу — во солдаты. <br>
  Уж никто-то по мне, молодце, не тужит; <br>
  Только тужит одна матушка родная. <br>
  Молодая ли жена проклинает, <br>
  Красны девушки про молодца вспоминают, <br>
  Весь род-племя, меня, молодца, провожают: <br>
  «Послужи там, добрый молодец, верой-правдой. <br>
  Положи за нас свою буйну головку!»<br>
  <span class='i'><br>
Русские народные 
  песни. / Сост. и вводн. тексты В.В. Варгановой.  М.: Правда, 1988. Раздел 
  «Песни солдат и о солдатах. Рекрутские причитания».</span><br>
