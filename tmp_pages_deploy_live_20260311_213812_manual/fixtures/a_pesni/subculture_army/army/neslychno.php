<br>
Стихотворение Николая Щербины &quot;После битвы&quot; из цикла &quot;Греческие 
мелодии&quot; написано в Таганроге в 1843 году в подражание песням греческих &quot;клефтов&quot; 
- пиратов-партизан, и посвящено гимназическому товарищу Щербины греку М.Е. Капараки. 
Впервые опубликовано в 1844 г. под заглавием &quot;Моряк&quot; в цикле &quot;Греческие 
мелодии&quot;. В 1852 г. стихотворение было положено на музыку Александром Гурилевым. 
Этот романс стал  популярен во время начавшейся вскоре Крымской войны 
1853-1856 гг., в том числе на флоте. На его основе не позднее грани XIX-XX вв. 
сложилась известная матросская песня <a href="raskinulos.php">&quot;Раскинулось 
море широко...&quot;</a> (др. название - &quot;Кочегар&quot;, обработка текста 
Г. Д. Зубарева), причем в песне мелодия стала проще.<br>
