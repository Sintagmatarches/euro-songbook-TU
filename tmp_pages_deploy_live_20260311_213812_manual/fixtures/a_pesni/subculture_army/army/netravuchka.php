	<h1>НЕ ТРАВУШКА, НЕ КОВЫЛУШКА...</h1>
<br>
	Не травушка, не ковылушка <br>
  В поле шатается – <br>
  Он шатался, волочился <br>
  Раздобрый молодец. <br>
  Идет он, шатается, <br>
  За ковыл-траву хватается. <br>
  Из его свежих ран <br>
  Кровь цевкой течет. <br>
  На нем черкесочка <br>
  Нараспашечку, <br>
  У черкесочки рукавички <br>
  Назад позакиданы, <br>
  А полы под ремень <br>
  Позатыканы, <br>
  Горячей кровью <br>
  Все призабрызганы. <br>
  Навстречу раздоброму молодцу <br>
  Мать родимая. <br>
  «Ты дите ли мое, <br>
  Моя деточка, <br>
  Ты на что допьяна напиваешься?» <br>
  — «Напоил меня <br>
  Русский царь, <br>
  Напоил меня тремя пойлами: <br>
  Первое пойло — пулечка свинцовая, <br>
  Второе — шашка острая, <br>
  Третье — пика длинная». <br>
  <span class='i'><br>
Русские народные песни / Сост. и вводн. тексты В.В. Варгановой. 
   М.: Правда, 1988. Раздел «Песни солдат и о солдатах. Рекрутские причитания».</span><br>
 <br> <br>
