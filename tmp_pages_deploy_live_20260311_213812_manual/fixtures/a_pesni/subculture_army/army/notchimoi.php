	<h1>НОЧИ МОИ ТЕМНЫЕ, ВЕЧЕРА ВЕСЕЛЫЕ!..</h1>
	<br>
	Ночи мои темные, вечера 
  веселые! <br>
  Уж я все ночи просиживаю, <br>
  Уж я все-то думы передумаю; <br>
  Как одна дума с ума нейдет; <br>
  Кабы были у меня сизы крылушки, <br>
  Еще б были б у меня золотые перушки, <br>
  Воспорхнул бы, воспорхнул бы высоко, <br>
  Полетел бы далекохонько, <br>
  Я слетал бы на свою сторону, <br>
  Уж я сел бы на широкий на свой двор, <br>
  Поглядел бы, посмотрел бы я <br>
  Что на свой ли на высок на терем. <br>
  Как в том-то терему стоят три стола. <br>
  За первым-то столиком родной батюшка сидит, <br>
  За другим за столиком родна матушка, <br>
  За третьим за столиком молода жена <br>
  Со малыми-то со детушками. <br>
  Уж вы дети мои, дети сирые, <br>
  Еще нет-то у вас родна батюшки!<br>
<br>
