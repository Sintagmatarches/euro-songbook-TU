	<h1>НУ, 
РЕБЯТА, МАРШ ДОМОЙ! </h1>
	
Солдатская песня-припевка<br>
<br>
Ну, ребята, марш домой, <br>
Вольно, врассыпную; <br>
Службу кончили и пой, <br>
Гаркнем удалую: <br>
<br>
Трай, рай, рита-тай, <br>
Гаркнем удалую!<br>
<br>
Было время - Царь смотрел, <br>
Молвил нам спасибо; <br>
В битвах полк наш не робел, <br>
В нуждах рак был рыба. <br>
<br>
Трай, рай, рита-тай, <br>
В нуждах рак был рыба. <br>
<br>
Громче музыка гуди, <br>
Весело с тобою! <br>
Слышь, ретивое в груди <br>
Словно рвется к бою. <br>
<br>
Трай, рай, рита-тай, <br>
Словно рвется к бою. <br>
<br>
По пути нам стал народ, <br>
В окна смотрят лица: <br>
Воин русский не урод -<br>
Взгляньте, молодицы! <br>
<br>
Трай, рай, рита-тай, <br>
Взгляньте, молодицы! <br>
<br>
Ну, ребята, не дремать, <br>
Живо да дружнее! <br>
Время кашу, щи хлебать - <br>
С дна погорячее. <br>
<br>
Трай, рай, рита-тай, <br>
С дна погорячее! <br>
<br>
<span class='i'>Все припевы повторяются хором.<br>
