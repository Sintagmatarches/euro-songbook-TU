	<h1>ОДНАЖДЫ ОСЕННЕЙ 
ПОРОЙ </h1>
<br>
Однажды осенней порой,<br>
  Падая, листья шуршали,<br>
  Я прибыл в родной городок,<br>
  Где дома меня ожидали.<br>
<br>
<em>Припев:</em><br>
<br>
Ты не пришла к поезду встретить матроса,<br>
  Я сам могу тихо войти,<br>
  Калиткою скрипнуть без спроса.<br>
<br>
К матросской груди я прижал,<br>
  И, нежно лаская старушку,<br>
  Я тихо сказал ей: «Веди,<br>
  Веди меня в нашу избушку».<br>
<br>
<em>Припев:</em><br>
<br>
Где я рос лаской твоею согретый,<br>
  Где по крышам гонял голубей,<br>
  Порою, бывало, раздетый.<br>
<br>
За круглым домашним столом,<br>
  Полнее бокал наливая<br>
  Шипучим шампанским вином,<br>
  Я пью за тебя, дорогая.<br>
<br>
<em>Припев:</em><br>
<br>
За то, что смогла ты мне дать,<br>
  За то, что смогла меня ждать,<br>
  Как ждать ни одна не сумела.<br>
<br>
Пью я за тех матерей,<br>
  Что сына в морфлот провожают,<br>
  Потом из далеких морей<br>
  В слезах на пороге встречают.<br>
<br>
<span class='i'>В нашу гавань заходили 
  корабли. Вып. 5. М.: Стрекоза, 2001.</span><br>
