	<h1>ПАМЯТИ РУССКИХ ГЕРОЕВ </h1>
 
  Слова Е. Хмельницкой <br>
  <br>
  Из лилий нежных, роз отменных,<br>
  Мы дружно им венки сплетём,<br>
  Героев наших незабвенных<br>
  Мы в гимнах звонких воспоём. <br>
  <br>
  Их имена, нам дорогие,<br>
  Передадим из рода в род,<br>
  Могилы, кровью залитые,<br>
  Чтить будет русский весь народ.<br>
  <br>
  Из лилий нежных, роз душистых,<br>
  Отчизна им венки сплетёт.<br>
  Сонм ангелов их в песнях чистых<br>
  К святому небу вознесёт.<br>
  <br>
  <span class='i'>Из архива проф. Владимира Ивановича Извекова &lt;IzvekovVI @ 
  mpei.ru&gt;, прислано им 3.11.2009.<br>
