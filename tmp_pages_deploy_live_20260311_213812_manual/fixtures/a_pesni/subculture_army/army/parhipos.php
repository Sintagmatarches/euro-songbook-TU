	<h1>ПОДВИГ 
АРХИПА ОСИПОВА (22 марта 1840 г.)</h1>
Песня 77-го Пехотного Тенгинского полка 
<br>
<br>
Как черкесы к нам ходили<br>
  Лет уж будет с пятьдесят.<br>
  Раз, два! Меж собой они решили<br>
  Резать наших жен-ребят.<br>
<br>
А в Михайловском на взморье<br>
  В укреплении сидят,<br>
  Раз, два! Всё Тенгинцы – горцам горе, -<br>
  Там давно они стоят.<br>
<br>
Подошли черкесы грозно,<br>
  Тысяч, кажется, с пяток,<br>
  Раз, два! С шумом, с гиком скачут грозно,<br>
  Мчатся с гор как злой поток.<br>
<br>
А Тенгинцы молодцами<br>
  Не считая тут врагов,<br>
  Раз, два! И картечью и штыками<br>
  Их укладывали в ров.<br>
<br>
Но врагам пришла подмога,<br>
  Стали снова напирать.<br>
  Раз, два! В помощь нам была подмога,<br>
  Чтоб нам крепость не сдавать.<br>
<br>
«Крепче, братцы, не сдаваться!<br>
  Здесь положим мы живот!<br>
  Раз, два! Равны мы теперь сражаться -<br>
  Русских много славных рот.<br>
<br>
Пусть врагам известно будет,<br>
  Что за Русская земля!<br>
  Раз, два! Враг нас сдаться не принудит,<br>
  Ляжем все здесь за Царя!» -<br>
<br>
Так по долгу по святому<br>
  Архип Осипов сказал.<br>
  Раз, два! К погребу пороховому<br>
  С фитилем у входа встал.<br>
<br>
Все враги вдруг побледнели -<br>
  Страшен был он с фитилем!<br>
  Раз, два! Вместе с погребом взлетели<br>
  И погибли все огнем.<br>
<br>
В перекличке по уставу<br>
  Вызывается Архип.<br>
  Раз, два! Отвечают, что за Веру,<br>
За Отечество погиб!<br>
<br>
<span class='i'>Расшифровка фонограммы Мужского хора Института певческой культуры 
«Валаам», дирижер И. Ушаков, запись 1997 г. CD «Песенная летопись Кавказской войны», 
СПб: Русская Лира, 2002.</span><br>
