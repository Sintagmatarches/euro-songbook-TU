<br>
<em>Черкесы</em> – во времена Кавказской войны 1817-1864 гг. черкесы было собирательным названием для  
горцев, сражавшихся с русскими. <br>
<br>
<br>
<span class='bold'>Гибель укрепления Михайловское</span><br>
<br>
В начале 1840 г. восстали ранее спокойные горцы черноморского побережья Кавказа. 
Недовольство было вызвано строительством русскими укреплений Черноморской береговой 
линии, разрушением вследствие этого местной экономики, а также голодом из-за неурожая 1839 г. 
19 февраля горцы захватили недостроенный редут Лазарева, 13 марта – укрепление 
Вельяминовское, а 29 марта лазутчик донес о намерении атаковать укрепление Михайловское, 
где находились 500 человек Тенгинского пехотного полка под командой штабс-капитана 
Лико. Гарнизон подтвердил ранее данное обещание живыми не сдаваться и в крайнем 
случае взорвать пороховой погреб. Рядовой Архип 
Осипов добровольно вызвался взорвать погреб, как только неприятель начнет сбивать 
с двери замки. С вечера 30 марта каждую полночь гарнизон выходил на стены, а Архипа 
Осипова с фитилем запирали в погребе. Горцы атаковали рано утром 3 апреля (22 
марта ст. ст.). К 10 утра защитники были перебиты, и горцы начали сбивать замки 
с погреба. Осипов взорвал погреб вместе с собой и нападавшими.<br>
<br>
Архип Осипов впервые в истории Русской армии был навечно зачислен в списки полка. 
В приказе №79 военного министра А. И. Чернышева значится:<em> &quot;Для увековечения 
же памяти о достохвальном подвиге рядового Архипа Осипова, который семейства не 
имел, его императорское величество высочайше повелеть соизволил сохранить навсегда 
имя его в списках 1-й гренадерской роты Тенгинского пехотного полка, считая его 
первым рядовым, и на всех перекличках при спросе этого имени первому за ним рядовому 
отвечать: &quot;Погиб во славу русского оружия в Михайловском укреплении&quot;. 
</em>В честь Архипа Осипова назван поселок Архипо-Осиповка, курорт Краснодарского 
края.<br>
