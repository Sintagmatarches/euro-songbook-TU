	<h1>ПЕРЕПЕТУЯ </h1>
<br>
Выходи на свиданье прощальное,<br>
В саду летнем тебя буду здать.<br>
Обману я вниманье дневального<br>
И приду ьебя целовать!<br>
<br>
Припев:<br>
<br>
Так поцелуй же ты меня, Перепетуя!<br>
Я тебя так безумно люблю!<br>
Эх, люблю!<br>
Для тебя чем угодно рискуя,<br>
Саковал, сакону, сакону!<br>
<br>
Обниму я тебя, как обмоткою,<br>
Как винтовку, прижму я к плечу,<br>
В твои губы прямою наводкою<br>
Поцелуев штук двадцать вкачу!<br>
<br>
Я куплю тебе фунтик мыла,<br>
Хочешь - мойся, а хочешь - торгуй!<br>
Для того, чтобы крепче любила,<br>
Поцелуй, поцелуй, поцелуй!<br>
<br>
Я кровать твою воблой обвешаю,<br>
Чтобы лучше и крепче был твой сон!<br>
Для тебя этой воблы достану я,<br>
Если хочешь, то целый вагон!<br>
<br>
Проведу я с тобой ночку темную,<br>
А наутро меня наш комвзвод<br>
За отлучку мою самовольную<br>
На "губу" под ружьем отведет!<br>
