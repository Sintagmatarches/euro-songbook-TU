	<h1>ПЛЫВЕТ ПО МОРЮ ФЛОТ КОРАБЛЕЙ </h1>
  <br>
  Плывет по морю флот кораблей, <br>
  Словно стадо лебедей, лебедей, <br>
  О, о, о, о-хо, хо, хо! <br>
  Словно стадо лебедей. <br>
  <br>
  Море синее и стонет, и гудит, <br>
  Плыть скорее тем корабликам велит. <br>
  О, о, о, о-хо, хо, хо! <br>
  Плыть скорее тем корабликам велит. <br>
  <br>
  Что ко тем ли ко турецким берегам, <br>
  На которых не впервой бывать уж нам. <br>
  О, о, о, о-хо, хо, хо! <br>
  На которых не впервой бывать уж нам. <br>
  <br>
  Паруса-то надуваются, шумят, <br>
  А на палубах солдатики стоят. <br>
  О, о, о, о-хо, хо, хо! <br>
  А на палубах солдатики стоят. <br>
  <br>
  Как на палубах солдатики стоят, <br>
  Меж собою разговоры говорят, <br>
  О, о, о, о-хо, хо, хо! <br>
  Меж собою разговоры говорят. <br>
  <br>
  "Придет время - нам потешиться пора, <br>
  Дрогнут турки от солдатского "Ура!" <br>
  О, о, о, о-о, хо, хо! <br>
  Дрогнут турки от солдатского "Ура!" <br>
  <br>
  <span class='i'>Хоровая песня "морской пехоты" времен Русско-турецкой войны 1828-1829 годов. 
  Солдаты не раз высаживались на турецкий берег не только в XVIII и XIX веках, 
  но и при легендарном князе Олеге, первым повесившем свой щит на вратах Царьграда, 
  ставшего после падения Византийской империи в 1423 году Стамбулом. В Царьград 
  не раз ходили русские богатыри, так что в солдатской песне говорится далеко 
  не случайно, что корабли плывут "ко турецким берегам, на которых не впервой 
  бывать уж нам". <br>
  <br>
  Антология военной песни / Сост. и автор предисл. В. Калугин. 
  М.: Эксмо, 2006<br></span>
  <br>
  <br> 
  Песня с сильным литературным влиянием, все куплеты, кроме первого, попадают в ритм шестистопного хорея, который использовался как стилизация одного из народных плясовых размеров (трехиктного тактовика).<br>

