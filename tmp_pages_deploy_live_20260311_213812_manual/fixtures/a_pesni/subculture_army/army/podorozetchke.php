    <h1>ПО ДОРОЖЕЧКЕ БЫЛО ПО ШИРОКОЙ...</h1>
<br>
	По дорожечке было по широкой, 
  <br>
  По широкой по московской, <br>
  Тут-то шла-прошла силушка армия, <br>
  Сила армия да конна гвардия, <br>
  Конна гвардия, три полка солдат, <br>
  Три полка солдат да молодых ребят. <br>
  Все молодые да безбородые, <br>
  Все холостые да неженатые, <br>
  Неженатые да кудреватые, <br>
  Они были-то царя белого, <br>
  Царя белого да Петра Первого. <br>
  Впереди-то полка да знамена несут, <br>
  Посреди полка да барабаны бьют, <br>
  Позади полка ружья светеют. <br>
  Ружья светлые да замки крепко бьют. <br>
  Замки сбрякали, солдаты всплакали, <br>
  Во поход пошли да в путь-дорожечку. <br>
  Хоть не в дальную, да во печальную, <br>
  Во печальную да во слезливую, <br>
  Во слезливую да несчастливую. <br>
  Во слезах они думу думали, <br>
  Думу думали да речь говорили: <br>
  «Уж мы где будем нонче день дневать, <br>
  Будем день дневать, ночь коротать? <br>
  Будем день-то дневать во сыром бору. <br>
  Ночь коротать — во темном лесу, <br>
  Под сосенкой да под кудрявою, <br>
  Во постелюшке — да мать сыра земля, <br>
  Во изголовьице — да зло кореньице, <br>
  Одеялышко — да буйны ветрички, <br>
  Разбужаньице — пуля быстрая, <br>
  Пуля быстрая да сабля вострая, <br>
  Умываньице — да кровь горячая, <br>
  Утираньице — да мурава-трава, <br>
  Воспитаньице — да сухари-вода, <br>
  Сухари-вода — солдатам еда. <br>
<br>
<span class='i'>Русские народные 
