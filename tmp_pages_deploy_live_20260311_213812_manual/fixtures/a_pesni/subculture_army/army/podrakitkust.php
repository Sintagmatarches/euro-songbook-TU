	<h1>ПОД 
РАКИТОВЫМ КУСТОМ</h1>
	
<br>
Под ракитовым кустом <br>
Казак голову склонил, <br>
Казак голову склонил, <br>
Турок шашку обнажил. <br>
Покатилась с плеч головка, <br>
Она на землю долой, <br>
Полилася кровь горяча <br>
На желтой, мелкий песок. <br>
Как над этой головой <br>
Вьется черный ворон. <br>
Ты не вейся, черный ворон, <br>
Над моею головой: <br>
Не добьешься ты добычи; <br>
Черный ворон, я не твой! <br>
Ты лети-ка, черный ворон, <br>
К отцу, к матери к моей.<br>
Ты скажи же, черный ворон, - <br>
Не убит в поле лежу, <br>
Ты скажи же, черный ворон, <br>
Что засватанный хожу: <br>
Засватала молодца <br>
Чужа дальня сторона, <br>
Чужа дальня сторона, <br>
Шашка острая стальна, <br>
Шашка острая стальна. <br>
Пуля резва свинцова. <br>
<br>
<span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006</span><br>
