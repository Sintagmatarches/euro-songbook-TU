	<h1>ПЕСНЯ ПОДВОДНИКА 
</h1>
<br>
Ты провожала меня в порту,<br>
  Капала тихо слеза.<br>
  Я целовал твои волосы<br>
  И голубые глаза.<br>
<br>
Девушка, милая девушка,<br>
  Тебя я не в силах забыть.<br>
  За то, что умела подводника<br>
  Три года ждать и любить.<br>
<br>
Если ж придется когда-нибудь<br>
  Мне в океане тонуть,<br>
  То на твою фотографию<br>
  Я не забуду взглянуть.<br>
<br>
Все растеряю на дне морском:<br>
  Сон, тишину и покой.<br>
  Даже твою фотографию<br>
  Вырвет акула с рукой.<br>
<br>
Буду лежать я на дне морском<br>
  Грудою белых костей,<br>
  Вот что такое романтика<br>
  В жизни сердечной моей.<br>
<br>
Ты провожала меня в порту,<br>
  Капала тихо слеза.<br>
  Я целовал твои волосы<br>
  И голубые глаза.<br>
