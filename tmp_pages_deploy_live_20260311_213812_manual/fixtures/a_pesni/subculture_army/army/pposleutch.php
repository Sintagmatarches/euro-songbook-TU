  <em>Припев. <br>
  </em><br>
  <span class='i'>Песня примечательна своими строками "С командиром-хватом / Любо, братцы, 
  жить!", из которых, по всей вероятности, и возникла <a href="../kazaki/lubo.htm">популярнейшая 
  народная песня</a>. Но это только предположение. Ведь и сам Николай Веревкин 
  мог взять эти строки из народной песни, скорее всего, разбойничьей или казачьей. 
  Для солдатской песни они не очень-то органичны и выглядят вставкой. Тем более 
  что Веревкин широко использовал в своих песнях народные мелодии и мотивы. <br>
  <br>
Антология военной песни / Сост. и автор предисл. В. Калугин. 
   М.: Эксмо, 2006.</span><br>
