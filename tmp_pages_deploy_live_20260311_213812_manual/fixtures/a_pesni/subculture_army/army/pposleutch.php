  Слова Николая Веревкина<br>
  На голос: Уж как шли наши под ружья<br>
  <br>
  Молодцам-солдатам <br>
  Не о чем тужить! <br>
  С командиром-хватом <br>
  Любо, братцы, жить! <br>
  <br>
  <em>Припев:</em><br>
  <br>
  Ширин, вырин, ристафор, <br>
  Фор, фор, фор, ер, ер, ерцу, <br>
  <div class='refren'>Ширин ди рай, <br>
  Рай, рай, рай, </div>
  Раз таки раз, <br>
  Радости, веселости мои! <br>
  <br>
  Он нас не печалит, <br>
  Он нас не гнетет, <br>
  Но за дело хвалит <br>
  И за дело бьет. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  Мы ж, без огорченья, <br>
  Выстроив ряды, <br>
  Ходим на ученья <br>
  Повторять зады. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  И до переклички <br>
  Трем песком штыки, <br>
  Белим аммунички, <br>
  Чистим репейки. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  Пригоняем ранцы, <br>
  Ставим их рядком <br>
  И наводим глянцы <br>
  Костяным зубком. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  А пригнав все гладко, <br>
  Целу ночь потом <br>
  Спим в палатках сладко <br>
  Богатырским сном. <br>
  <br>
  <em>Припев.</em><br>
  <br>
  Право, жизни нашей <br>
  Веселее нет! <br>
  Водка да щи с кашей <br>
  Есть у нас в обед. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  Красные девицы, <br>
  Нарядясь в атлас, <br>
  Собой круглолицы, <br>
  Даром любят нас. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  Так зато уж, братцы, <br>
  Вышел на парад, <br>
  Каждый рад стараться <br>
  Подтянуть приклад. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  Лишь бы шагом плавным <br>
  Угодить отцам <br>
  Командирам главным, <br>
  Светам-молодцам. <br>
  <br>
  <em>Припев. </em><br>
  <br>
  Дай Бог этим хватам <br>
  Многи лета жить! <br>
  С ними нам, солдатам,<br>
  Не о чем тужить! <br>
