<br>
<strong>Малышев Григорий Григорьевич (1802-1868)</strong> - солдат, поэт, военный 
музыкант, прошел две войны, включая русско-турецкую 1828-1829 гг., получив чин унтер-офицера. 
В 1835 г. за исполнение своей песни &quot;Русский царь набрал дружину...&quot; перед 
монархами России, Пруссии и Австрии, переведен в придворную певческую капеллу.<br>
