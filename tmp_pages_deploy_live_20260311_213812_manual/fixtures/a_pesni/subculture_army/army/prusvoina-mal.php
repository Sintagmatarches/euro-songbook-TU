Слова Григория Малышева<br>
<br>
Буйный ветер воет <br>
<div class='refren'>С полуночи, </div>
Месяцем стан русский <br>
<div class='refren'>Осветился, </div>
Ратные доспехи <br>
<div class='refren'>Осребрились; </div>
На копье склонившись, <br>
<div class='refren'>Юный воин </div>
Пел про край родимый <br>
<div class='refren'>Одиноко: </div>
"Взвейся ветерочек <br>
<div class='refren'>Легкокрылый, </div>
Отнеси мой голос <br>
<div class='refren'>В край родимый! </div>
Скажи моим кровным: <br>
<div class='refren'>Я храним, </div>
Их благословеньем, <br>
<div class='refren'>Как щитом. </div>
Скажи моей милой: <br>
<div class='refren'>Что я жив, </div>
Что о ней вздыхаю <br>
<div class='refren'>День и ночь. </div>
Завтра нас осветит <br>
<div class='refren'>Солнышко, </div>
В битве, где, быть может, <br>
<div class='refren'>Мертв паду. </div>
О! тогда ты ветер <br>
<div class='refren'>Вихрем мчись </div>
В край родной, на север, <br>
<div class='refren'>В отчий дом. </div>
Дай там весть, что пал я <br>
<div class='refren'>С честию, </div>
За Святую веру, <br>
<div class='refren'>Нашу мать! </div>
За свою отчизну, <br>
<div class='refren'>За Царя!" </div>
<br>
1828 <br>
