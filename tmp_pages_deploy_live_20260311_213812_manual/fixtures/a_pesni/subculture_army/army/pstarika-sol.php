Слова Владимира Соллогуба<br>
<br>
Ты помнишь, брат, те времена, <br>
Когда с тобой на ратной службе <br>
Одни родные знамена <br>
Сзывали нас к горячей дружбе? <br>
<br>
Теперь мы дожили зимы: <br>
Глядим в отверзтую могилу. <br>
Но схороним до смерти мы <br>
В душе огонь и в дружбе силу. <br>
<br>
Мы памятью одной живем <br>
Теперь, мой друг, в немом покое, <br>
И не гуляем мы вдвоем <br>
В пиру штыков и в шумном бое. <br>
<br>
Но если крикнет край родной, <br>
Да про врагов опять нам скажет, <br>
Тряхнем, товарищ, стариной, <br>
И старина себя покажет. <br>
