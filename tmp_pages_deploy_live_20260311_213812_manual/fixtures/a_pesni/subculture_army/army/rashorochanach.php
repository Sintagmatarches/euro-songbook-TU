	 Расхороша наша барыня <br>
  Что Арина-то Ивановна: <br>
  Разорила село теплое Пилютино, <br>
  Раздала крестьян в солдатушки, <br>
  Во молоденьки рекрутики. <br>
  Охотники наперед идут, <br>
  Невольнички позади тянутся, <br>
  Охотнички песню взгаркнули, <br>
  Невольнички слезно всплакнули, <br>
  Свою сторону вспомянули: <br>
  «Сторона ль моя, сторонушка, <br>
  Гулливая наша, прохладливая».<br>
