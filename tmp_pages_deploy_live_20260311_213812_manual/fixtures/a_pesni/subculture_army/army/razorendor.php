	<h1>РАЗОРЕННАЯ 
ДОРОЖКА</h1>
<br>
Разоренная дорожка <br>
От Можая до Москвы, ах! <br>
Разорил эту дорожку <br>
Неприятель — злой француз.<br>
<br>
Разоривши ту дорожку, <br>
В свои земли жить пошел, ах! <br>
Как расхвастался Парижем, <br>
Что своим ли Парижом.<br>
<br>
Не хвались, француз, напрасно <br>
Что своим ли Парижом; <br>
Есть у нас город Москва - <br>
Все получше Парижа! <br>
<br>
На горе стоит Москва, <br>
Вся садами убрана, ах! <br>
Белым камнем выстлана, <br>
Песком желтым сыпана.<br>
<br>
Белым камнем выстлана, <br>
Песком желтым сыпана, ах! <br>
Распрекрасная Москва — <br>
Всей России красота. <br>
