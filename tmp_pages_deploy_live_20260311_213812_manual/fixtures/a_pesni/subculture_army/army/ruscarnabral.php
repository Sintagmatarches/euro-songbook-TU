  ЦАРЬ НАБРАЛ ДРУЖИНУ </h1>
  Слова Григорий Малышева<br>
  <br>
  Русский царь набрал дружину <br>
  И велел своим орлам <br>
  Плыть по морю, на чужбину, <br>
  В гости к добрым пруссакам. <br>
  <br>
  "Кровь царей нас породнила, <br>
  Славный бой нас подружил; <br>
  Сам Творец во бранях сильный <br>
  Наш союз благословил. <br>
  <br>
  Если зависть и восстанет <br>
  Разорвать союз друзей, <br>
  Там следа врагов не станет, <br>
  Где пройдут полки царей. <br>
  <br>
  Не на бой летим сражаться, <br>
  Не крамольных усмирять, <br>
  Но с друзьями повидаться, <br>
  Пруссаков спешим обнять. <br>
  <br>
  Нас лелеет царь державный, <br>
  Слава Белому Царю! <br>
  Слава Руси православной, <br>
  И родному королю!" <br>
  <br>
  1835 <br>
  <br>
   <span class='i'>Малышев Григорий Григорьевич (1802-1868) - поэт, военный музыкант. В романсной 
  лирике имя Григория Малышева известно по одной из популярнейших "Троек" "Звенит 
  звонок, и тройка мчится...", но не менее известны и другие его песни, публиковавшиеся 
  как солдатские без имени автора. Сын унтер- офицера, он и сам прошел через две 
  войны, стал унтер-офицером, при этом "комплексным музыкантом", умевшим не только 
  исполнять, но и сочинять военные песни. Одна из них "Русский царь набрал дружину..." 
  была впервые исполнена Григорием Малышевым 5 сентября 1835 года в городе Калише 
  в присутствии трех монархов на освящении памятника в честь боевого союза 1813-1814 
  годов. В 1833 году был восстановлен Священный союз между Россией, Пруссией и 
  Австрией. Идею Священного союза во многом укрепляло упомянутое в песне "породнение". 
  В мае 1873 года Николай I присутствовал в Берлине на бракосочетании племянницы 
  с принцем Вильгельмом, а сам был женат на прусской принцессе Шарлоте-Фредерике-Вильгельмине. 
  Но эти династические браки не спасли Россию от грядущих исторических катастроф. 
  После премьеры песни Малышев "высочайшим повелением" был переведен "большим 
  певчим" в придворную певческую капеллу, где и прослужил почти четверть века, 
  выпустив в 1848 году сборник стихов и песен, свидетельствующих о его незаурядном 
  поэтическом даровании, оставшимся не замеченным современниками, хотя его "Тройка" 
  уже при жизни стала песенной классикой, а стихотворение "Христос воскресе!" 
  тоже могло войти в хрестоматии наряду с другими образцами русской религиозной 
  лирики. Но история поэзии, увы, тоже не имеет сослагательного наклонения. Во времена 
  некрасовского "Современника" стихи и песни певчего из придворной капеллы, проникнутые 
  "верноподданническими настроениями", могли вызвать лишь полное отторжение. В 
  начал Крымской войны он выпустил в свет "солдатские куплеты", которые тоже попали 
  в разряд ура-патриотических. Участь Малышева на поэтическом Олимпе была предрешена, 
  что никак не сказалось на судьбах его песен, живущих по иным неисповедимым законам. 
