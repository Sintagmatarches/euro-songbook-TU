Музыка неизвестного автора<br>
Слова Афанасия Фета<br>
<br>
Какой тут дышит мир! Какая слава тризны <br>
Средь кипарисов, мирт и каменных гробов! <br>
Рукою набожной сложила здесь  отчизна <br>
Священный прах своих сынов. <br>
<br>
Они и под землей отвагой прежней дышат... <br>
Боюсь, мои стопы покой их возмутят, <br>
И мнится, все они шаги живого слышат, <br>
Но лишь молитвенно молчат. <br>
<br>
Счастливцы! Высшею пылали мы любовью: <br>
Тут что ни мавзолей, ни надпись - всё боец, <br>
И рядом улеглись, своей залиты кровью, <br>
И дед со внуком и отец. <br>
<br>
Из каменных гробов их голос вечно слышен, <br>
Им внуков поучать навеки суждено, <br>
Их слава так чиста, их жребий так возвышен, <br>
Что им завидовать грешно... <br>
<br>
4 июня 1887 <br>
