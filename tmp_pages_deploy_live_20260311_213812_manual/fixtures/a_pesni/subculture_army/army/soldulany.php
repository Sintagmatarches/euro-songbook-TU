	<h1>СОЛДАТУШКИ 
УЛАНЫ... </h1>
<br>— Солдатушки уланы, <br>
  У вас лошади буланы, <br>
  Где были, побывали? — <br>
  Мы под Киевом стояли <br>
  Со французом воевали, <br>
  Вдоль деревни проезжали, <br>
  В крайний дом заезжали, <br>
  Просились ночевать. — <br>
  Вдовушка выходила, <br>
  С ними речи говорила: <br>
  «У нас дворик маленек, <br>
  Хаточка тесненька!» <br>
  — Нас солдатушек немножко, <br>
  Полтораста нас на конях, <br>
  Ровно двести пешеходу. — <br>
  Они на дворик уезжали, <br>
  У горницу уходили, <br>
  Садились все по лавкам, <br>
  Все по лавкам, <br>
  По порядкам!<br>
  Один солдат впереди <br>
  Под окошечком, <br>
  А вдовушка на шесточке. <br>
  А большой солдат <br>
  Слово молвил: <br>
  «Ты вдовушка молодая! <br>
  Подойди, вдова, поближе, <br>
  Поклонися мне пониже, <br>
  Ты сыми-ка со крючка черную шляпу, <br>
  У шляпушке полотенце – <br>
  Не твово ли рукодельями <br>
  В полотенце узелочек, <br>
  Там завязан перстенечек, <br>
  С которым мы с тобой венчались, <br>
  Которым обручались». <br>
