	<h1>ТАМАРА 
</h1>
<br>
Я помню тот вечер,<br>
Горячие встречи.<br>
С любимой подругой я в парке сидел.<br>
Звенела гитара,<br>
Кружилися пары,<br>
А я под гитару всё пел и всё пел:<br>
<br>
«Тамара, Тамара,<br>
Послушай гитару!<br>
Пусть сердце забьется от нашей любви.<br>
Пусть сердце забьется,<br>
И слезы польются,<br>
И в жарких объятьях окажешься ты».<br>
<br>
Вот минуло, братцы,<br>
Мне лет восемнадцать.<br>
Прислали в повестку, одел я шинель. <br>
Трещали морозы,<br>
Прощались сквозь слезы,<br>
А я под гитару всё пел и всё пел:<br>
<br>
«Тамара, Тамара,<br>
Послушай гитару!<br>
Пусть сердце забьется от нашей любви.<br>
Пусть сердце забьется,<br>
И слезы польются,<br>
И в жарких объятьях окажешься ты».<br>
<br>
Вот минул уж, братцы,<br>
Мне год двадцать первый.<br>
На службе женился, домой прилетел.<br>
Иду по бульвару <br>
Навстречу Тамаре.<br>
Последний разочек ей песенку спел:<br>
<br>
«Тамара, Тамара,<br>
Теперь мы не пара.<br>
Люби ты другого, Тамара моя.<br>
Забудь наши встречи,<br>
Те милые речи,<br>
Забудь про гитару, забудь про меня!»<br>
