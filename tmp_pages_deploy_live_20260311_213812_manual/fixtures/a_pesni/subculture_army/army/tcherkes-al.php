	<h1>ЧЕРКЕС 
</h1>
Музыка Александра Алябьева<br>
Слова Василия Алябьева<br>
<br>
Обвернут буркою широкой, <br>
И лук, и стрелы за спиной, <br>
Черкес над пропастью глубокой <br>
Стоит... И вскрикнул удалой: <br>
"Что горы вздрогнули от гула? <br>
Что грозно ропщет по реке? <br>
Не стало Трамова аула!" <br>
А шашка замерла в руке. <br>
<br>
О конь, товарищ быстроногий, <br>
Нагорных скакунов краса, <br>
Какою скачешь ты дорогой, <br>
Какие видишь небеса? <br>
Твой ныне корм - пшеница ль яра? <br>
В твоем же тесном деннике <br>
Разгулье стало от пожара - <br>
А шашка замерла в руке... <br>
<br>
Нет сакли, где черкес свободный <br>
На ложе неги отдыхал <br>
И сладостно кумыс холодный <br>
Из чаши братской выпивал. <br>
Нет ружей; нет одежд богатых; <br>
Нет памяти о кунаке, <br>
Даров тех дружбы верной взятых -<br>
А шашка замерла в руке... <br>
<br>
"Наездников исчезли силы, <br>
Как дымный пар от облаков; <br>
Нет храбрым на земле могилы; <br>
Сдружился с ними врана клев; <br>
Но удалому ль согласиться, <br>
Что на Эльбрусе плена звук?!" <br>
Сказал - и в бездну он стремится, <br>
И шашка выпала из рук! <br>
