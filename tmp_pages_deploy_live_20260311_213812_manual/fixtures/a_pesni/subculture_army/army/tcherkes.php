	<h1>ЧЕРКЕССКАЯ 
  ПЕСНЯ</h1>
 
  Музыка Александра Алябьева<br>
  Слова Александра Пушкина<br>
  <br>
  В реке бежит гремучий вал;<br>
  В горах безмолвие ночное;<br>
  Казак усталый задремал,<br>
  Склонясь на копие стальное.<br>
  Не спи, казак: во тьме ночной<br>
  Чеченец ходит за рекой.<br>
  <br>
  Казак плывет на челноке,<br>
  Влача по дну речному сети.<br>
  Казак, утонешь ты в реке,<br>
  Как тонут маленькие дети,<br>
  Купаясь жаркою порой:<br>
  Чеченец ходит за рекой.<br>
  <br>
  На берегу заветных вод<br>
  Цветут богатые станицы;<br>
  Веселый пляшет хоровод.<br>
  Бегите, русские певицы,<br>
  Спешите, красные домой:<br>
  Чеченец ходит за рекой.<br>
  <br>
  1820<br>
  <br>
  <span class='i'>Из поэмы «Кавказский пленник» (1820). Романсы Людвига Мауэра (1822), И.И. Геништы (1823), А.А. Алябьева (1829) и многих других композиторов (более 
  30). Наибольшую популярность получила алябьевская мелодия. Уже с 30-х годов 
  XIX века стала включаться в сборники казачьих и солдатских песен, прочно войдя 
  в «военный» репертуар. Позднее то же самое произойдет и с <a href="kazkolyb.php">«Казачьей 
  колыбельной»</a> Лермонтова, в которой звучат те же мотивы («Злой чечен ползет 
  на берег, / Точит свои кинжал»). В XX веке, во времена гражданской войны и в 
  среде русской эмиграции, одной из самых культовых стала песня на пушкинские 
  стихи <a href="oleg.php">«Как ныне сбирается вещий Олег»</a> с припевом «Так 
  громче музыка / Играй победу...» <br>
  <br>
  Антология русской песни / Сост., предисл. и коммент. Виктора 
  Калугина.  М.:  Эксмо, 2005.</span><br>
 <br>
  <br>
  Поэма &quot;Кавказский пленник&quot; была начата Пушкиным во время пребывания 
  его в Крыму и закончена в Каменке 23 февраля 1821 г. Опубликована в 1822 
  г. с многочисленными цензурными искажениями. Сам Пушкин был не вполне доволен поэмой. 
  Из письма Пушкина В.П. Горчакову, 1822 год: &quot;Характер моего пленника неудачен; 
  это доказывает, что я не гожусь в герои романтического стихотворения. Я в нем 
  хотел изобразить это равнодушие к жизни и к ее наслаждениям, эту преждевременную 
  старость души, которые сделались отличительными чертами молодежи 19 века. Конечно, 
  поэму приличнее было бы назвать &quot;Черкешенкой&quot; - я об этом не подумал&quot;.<br>
  <br>
  Еще до этого романса, в 1822-1825 гг. Александр Алябьев написал по &quot;Кавказскому 
  пленнику&quot; мелодраму из 23 номеров для симфонического оркестра, голоса и 
  чтеца (в 1859 г. ее <a href="../romans/alabiev/kavkazplen.php">аранжировал для фортепиано</a> 
  Александр Дюбюк). В 1843 г. Алябьев также положил на музыку <a href="tcherkesp-lerm.php">&quot;Черкесскую 
  песню&quot;</a> Лермонтова (&quot;Много дев у нас в горах...&quot;)<br>
  <br>
