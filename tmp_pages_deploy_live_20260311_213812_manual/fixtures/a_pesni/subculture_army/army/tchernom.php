	<h1>ЧЕРНОМОРОЧКА</h1>
<br>
Полюбила черноокая<br>
Моряка, красавца, сокола.<br>
Полюбила душой и любовью такой -<br>
Словно море глубокое.<br>
<br>
Как с такой матросу не дружить?<br>
Как такой ему не дорожить?<br>
Ах, какая черноморочка,<br>
Как такую не любить?<br>
<br>
Ей букетами поклонники<br>
Забросали подоконники,<br>
Но она лишь душою своею верна<br>
Молодому подводнику.<br>
<br>
Пусть никто не обижается,<br>
Что один ей только нравится,<br>
С милым рядом она, как морская волна,<br>
Никогда не расстанется.<br>
