  <br>
  <br>
  Последняя русско-турецкая война (1877-1878) принесла независимость Болгарии 
  (в урезанных границах), Сербии, Черногории и Румынии и унесла 200 тыс. жизней 
  одних только русских солдат - в основном, из-за бездарного командования: &quot;показательный&quot; 
  провалившийся штурм Плевны в день именин Александра II, морозы на Шипке, голод 
  в окруженном Баязете. Погибло огромное количество турецких солдат, балканских 
  повстанцев и мирных жителей, в том числе в национальной резне. Обе стороны не 
  были готовы к войне, но дипломатические переговоры провалились, а проблема не 
  терпела отлагательств: на оккупированных Турцией территориях бывших славянских 
  государств уже два года длилось кровавое восстание и сражалось несколько тысяч 
  российских добровольцев. Русская армия в этой войне впервые воевала на основании 
  всеобщей воинской повинности, сменившей рекрутский набор. Некоторые слабые стороны  армии, выявленные на войне, - в частности, плохое качество стрелкового оружия, - вскоре были исправлены, однако, например, белая полевая форма, служившая идеальной мишенью для стрелков неприятеля, сохранялась вплоть до Первой мировой войны.<br>
