	<h1>ВОТ 
  ПОЛНОЧЬ НАСТУПАЕТ...<br>
  (Турецкая война 1877-1878 гг.)</h1>
  <br>
  Вот полночь наступает,<br>
  Луна горит светло,<br>
  Отряд наш выступает <br>
  С бивака своего.<br>
  <br>
  <em>Припев:</em><br>
  <br>
  Горныя вершины,<br>
  Увижу ль я вас вновь,<br>
  Турецкия долины –<br>
  Кладбища удальцов.<br>
  <br>
  Идем мы тихо, стройно,<br>
  Подходим к высотам;<br>
  Вершины эти грозно<br>
  Показывались нам.<br>
  <br>
  Начальник батареи<br>
  Подставил грудь свою.<br>
  «Ребята, не робейте, -<br>
  Не страшна смерть в бою».<br>
  <br>
  Тут турки все проснулись…<br>
  Дают сигнал в рожки…<br>
  Но мы не оробели<br>
  И двинулись в клинки.<br>
  <br>
  Вскочил на батарею<br>
  Хорунжий наш лихой,<br>
  Мы все за ним – стеною,<br>
  И кровь мы льем рекой.<br>
  <br>
<span class='i'>Записано 
