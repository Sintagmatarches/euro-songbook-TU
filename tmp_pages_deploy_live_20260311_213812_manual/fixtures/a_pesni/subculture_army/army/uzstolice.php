	<h1>УЖ 
  СТОЛИЦЕ ВОЗВЕСТИЛИ </h1>
  <br>
  Уж столице возвестили, <br>
  Слышно с пушечной пальбой,<br>
  <br>
  Как француза мы побили, <br>
  Удалой наш граф с тобой. <br>
  <br>
  Вишь, какой неугомонный,<br>
  За Двину к нам перешел;<br>
  <br>
  Враг коварный, лютый, злобный -<br>
  Сам напасть себе нашел.<br>
  <br>
  Молодца мы потеряли:<br>
  Кульнева злодей убил; <br>
  <br>
  Да зато и отчесали, - <br>
  Дорого ты заплатил!.. <br>
  <br>
  Сколько тысяч меж рядами <br>
  Ты своими не дочтешь? <br>
  <br>
  Сколько пулями, штыками, <br>
  На плечах ты ран несешь? <br>
  <br>
  Бог поможет нам Великий, <br>
  И погоним за Двину; <br>
  <br>
  Прочь, скорей беги, зверь дикий, <br>
  Да лечи свою спину!.. <br>
  <br>
  Между тем и Макдональда <br>
  Мы пойдем в Люцен искать; <br>
  <br>
  И его, побив, нам надо <br>
  За реку к тебе же гнать... <br>
  <br>
  Так, удалый граф, с тобою <br>
  Русские везде пройдут; <br>
  <br>
  Смерть и ужас пред собою <br>
  Вражьей силе понесут. <br>
  <br>
  Витгенштейн! С тобой готовы <br>
  В воду и огонь идти: <br>
  <br>
  Слава, честь, победы новы <br>
  Ожидают нас в пути! <br>
  <br>
  <span class='i'>Первая победа русских войск под Клястицами остановила продвижение двух колонн 
  маршалов Макдональда и Ундино на Петербург, прославив имена генерала Якова Кульнева 
  и главнокомандующего Петра Витгенштейна. Собственно, это была победа Кульнева, 
  сообщавшего в письме к брату: "Уведомляю тебя, что я, командуя авангардом, первый 
  из русских, который имел счастье сражаться с французским маршалом Ундино". Представленный 
  к Георгию, он заявил: "Если заслуживаю какое-либо воздаяние, то прошу за особую 
  милость наградить Ридигера вместо меня". И награду получил Ридигер - предок 
  патриарxa Алексия, ставший после гибели Кульнева командиром Гродненского гусарского 
  полка. Первой была не только победа. 20 июля 1812 года при переправе через Дриссу 
  под артиллерийским огнем Кульневу ядром оторвало ноги. Его предсмертные слова 
  облетели всю русскую армию: "Друзья, спасайте Отечество! Не уступайте врагу 
  ни шагу родной земли. Победа нас ожидает!" <br>
  <br>
  Такова предыстория этой солдатской песни, сохранявшейся в репертуаре военных 
  хоров в течение всего XIX века. Популярностью пользовался и "Марш на смерть 
  генерал-майора Кульнева", созданный князем Павлом Долгоруким в том же 1812 году. 
  <br>
