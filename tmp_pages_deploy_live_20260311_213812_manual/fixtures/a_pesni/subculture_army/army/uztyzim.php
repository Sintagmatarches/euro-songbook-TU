        <br> <h1>УЖ ТЫ, ЗИМУШКА, 
ЗИМА…</h1>
 <br>Уж ты, зимушка, зима, <br>
  Холодна зима была! <br>
  Холодна зима проходит, <br>
  Лето красно настает. <br>
  Лето красно настает, <br>
  У солдата сердце мрет. <br>
  У солдата сердце мрет, <br>
  Лето дома не живет. <br>
  Лето в лагерях стоять, <br>
  Поутру рано вставать. <br>
  Поутру рано вставать, <br>
  Лицо бело умывать. <br>
  Лицо бело умывать, <br>
  Мундир черный надевать. <br>
  Мундир черный надевать, <br>
  На ученье выезжать. <br>
  Нам ученье не мученье, — <br>
  Между прочим, тяжело. <br>
  Между прочим, тяжело, <br>
  Что не знаем ничего. <br>
 <br>
<span class='i'>Сборник русских 
  народных лирических песен Н.М. Лопатина и В.П. Прокунина. М., 1889. <br>
  <br>
  Русские песни / Сост. проф. Ив.Н. Розанов. М.: Гос. изд-во худож. литературы, 
  1952.</span><br>
 <br><br>
 Солдатская песня, скорее всего, 2-й п. XVIII в. (рифмованная, ориентируется на литературный 4-стопный хорей). См. также кавказский вариант - <a href="zim-kavkaz.php">"Ох, да ты, зимушка-зима"</a>. Зачин использовался в рабочих песнях (см. <a href="../labor/uztyzimuchka.htm">"Уж ты зимушка-зима"</a> - рабочая) и более поздних армейских (см. литературную красную песню Гражданской войны в Сибири <a href="../grvojna/kr/ehtyzim.php">"Эх, ты зимушка, зима"</a>).<br>
 <br>
 <br>
 <span class='bold'>ВАРИАНТ<br>
<br>
Ох ты, зимушка, зима </span><br>
<em>Солдатская хоровая песня </em><br>
<br>
Ох ты, зимушка, зима, <br>
Холодна очень была! <br>
<br>
<em>Припев (после каждого двустишия): </em><br>
<br>
Эй, эй, о-хо-хо! <br>
Черная галка, чистая полянка, <br>
Ты же, Марусенька, черноброва, <br>
Чего не ночуешь дома? <br>
<br>
Холодна очень была, - <br>
Все дороги замела. <br>
<br>
Все дороги, все пути, <br>
Негде к миленькой пройти! <br>
<br>
Негде к миленькой пройти, <br>
С нею время провести <br>
<br>
Холодна зима была, - <br>
Заморозила меня. <em><br>
</em><br>
Заморозила меня, <br>
Солдатика бравого. <br>
<br>
Солдатика бравого, <br>
Офицерика славного. <br>
<br>
Зима лютая проходит, <br>
Весна красна настает. <br>
<br>
Весна красна настает, <br>
У солдата сердце мрет. <br>
<br>
У солдата сердце мрет: <br>
Летом в лагерях стоять. <br>
<br>
Летом в лагерях стоять, <br>
Поутру рано вставать. <br>
<br>
Поутру рано вставать, <br>
Бело лицо умывать. <br>
<br>
Бело лицо умывать, <br>
Мундир черный надевать. <br>
<br>
Мундир черный надевать <br>
И винтовку в руки брать. <br>
<br>
Винтовочку в руки брать, <br>
На ученья выступать. <br>
<br>
На ученье ничего, <br>
Только больно тяжело. <br>
<br>
Стой-ка прямо, держись браво, <br>
Ружьем делай хорошо. <br>
<br>
Ружьем делай хорошо, <br>
Начальничкам весел<strong>о</strong>.<br>
<br>
<span class='i'>Антология военной песни / Сост. и автор предисл. В. Калугин. М.: Эксмо, 2006.</span><br>

