Уж вы горы мои, горы Воробьевские, <br>
  Ничего вы, горы, ничего не породили, <br>
  Породили горы один бел-горюч камень.<br>
  Из-под камушка течет, течет быстра реченька, <br>
  Как на этой на реке стоит куст ракитовый, <br>
  На кусту том сидит, сидит млад сизой орел, <br>
  Во когтях держит, держит черна ворона. <br>
  Он и бить его не бьет, всё выспрашивает: <br>
  «Где ты, ворон, братец, был, где полетывал?» <br>
  — «Уж и был я на диких, на диких степях на саратовских, <br>
  Уж и видел я там, видел чудо дивное: <br>
  Там лежит, лежит тело белое. <br>
  Никто к тому телу, никто не подступится. <br>
  Как вокруг того тела вьются лишь три ласточки. <br>
  Как и первая ласточка — его родна матушка, <br>
  А другая ласточка — его сестра родная, <br>
  Как и третья ласточка — его молода жена. <br>
  Как и мать по нем кричит, что быстра река течет. <br>
  А сестра по нем кричит, что с горы ручьи текут. <br>
  А жена по нем кричит — словно утрешня роса: <br>
  Как и солнышко взойдет — роса высохнет». <br>
