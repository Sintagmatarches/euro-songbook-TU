	<h1>ВАЛЬС 
БАЛТИЙСКИХ МОРЯКОВ</h1>
Песня 1950-х годов<br>
<br>
Помню день, мы на рейде стояли,<br>
  Нам Кронштадт улыбался вдали,<br>
  Моряки туда взор устремляли,<br>
  Целый месяц не видя земли.<br>
<br>
Наконец мы приказа дождались,<br>
  В увольненье отпущен был я,<br>
  И с веселой братвой распрощавшись,<br>
  Серый катер уносит меня.<br>
<br>
Вот подплыли, иду по бульвару,<br>
  Как на палубе, с креном на борт,<br>
  Все в порядке, наглажены шкары,<br>
  И с хорошим фасоном секспорт.<br>
<br>
Бляха тоже огни отражает,<br>
  Бескозырка сияет как снег,<br>
  Пусть теперь комендантский встречает,<br>
  Точка в точку по форме одет.<br>
<br>
Прохожу по тенистым аллеям,<br>
  По бокам зеленеют кусты,<br>
  Вдруг навстречу идет мне девчонка<br>
  Небывалой еще красоты.<br>
<br>
Голубые глаза словно море,<br>
  Ветер локон трепещет шутя,<br>
  Повстречал незнакомку я в сквере,<br>
  Мне понравилось это дитя.<br>
<br>
Потихоньку я стал швартоваться,<br>
  Флангом справа я к борту зашел,<br>
  Морякам ведь не надо теряться,<br>
  С незнакомкой я рядом пошел.<br>
<br>
А потом мы пошли по аллеям,<br>
  И на банку мы сели вдвоем,<br>
  Помню белые стройные ножки<br>
  И чуть-чуть приоткрывшийся рот.<br>
<br>
А потом мы танго танцевали,<br>
  Ее ручка легла на плечо,<br>
  Губки имя свое прошептали,<br>
  Грудь прильнула ко мне горячо.<br>
<br>
Незаметно минуты летели,<br>
  Увольненью кончались часы,<br>
  А она почему-то смотрела,<br>
  И шептала – «забудешь ведь ты!»<br>
<br>
И опять огни те же Кронштадта<br>
  Улыбались в дали голубой,<br>
  В тех аллеях осталися встречи,<br>
  И все танцы ушли за кормой.<br>
<br>
<span class='i'>В нашу гавань заходили 
  корабли. Вып. 4. М.: Стрекоза, 2001.</span><br>

