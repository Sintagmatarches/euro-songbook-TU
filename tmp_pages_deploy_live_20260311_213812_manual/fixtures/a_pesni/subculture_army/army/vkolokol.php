<h1>В 
КОЛОКОЛ, МИРНО ДРЕМАВШИЙ…</h1>
Слова Алексея К. Толстого<br>
<br>
В колокол, мирно дремавший, с налета тяжелая бомба <br>
Грянула; с треском кругом от нее разлетелись осколки; <br>
Он же вздрогнул, и к народу могучие медные звуки <br>
Вдаль потекли, негодуя, гудя и на бой созывая. <br>
<br>
3 декабря 1855 <br>
<br>
<span class='i'>Музыка Ц.А. Кюи (1877), Н.А. Маныкина-Невструева (1898), Н.М. Ладухина, 
