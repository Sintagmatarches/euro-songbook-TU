  <em>Храброму народу черногорскому </em><br>
  <br>
  Музыка Петра Щуровского<br>
  Слова С. Минского<br>
  <br>
  Чу! Раздался клич призывный, <br>
  Стон измученной страны, <br>
  Вестник воли всенародной, <br>
  Вестник праведной войны. <br>
  <br>
  Ураганом зов пронесся <br>
  По селам и городам <br>
  И могучим эхом грянул <br>
  По ущельям, по горам. <br>
  <br>
  С ярким знаменем свободы, <br>
  Словно Божий гнев, грозна, <br>
  Всколыхнулася, вздрогнула <br>
  Угнетенная страна. <br>
  <br>
  Звон торжественный несется <br>
  К Богу брани и скорбей. <br>
  Мать на пир благословляет, <br>
  Улыбаясь, сыновей. <br>
  <br>
  Как на праздник, в ярких платьях, <br>
  И отважны и смелы. <br>
  Собралися в батальоны <br>
  Черногорские орлы. <br>
  <br>
  Блещут черные их очи <br>
  Перед встречею с врагом, <br>
  И сверкают ятаганы <br>
  За пурпурным кушаком. <br>
  <br>
  Будет званный пир кровавый, <br>
  Дрогнет ужасом земля, <br>
  Обагрятся алой кровью <br>
  Неповинные поля. <br>
  <br>
  Но та кровь и эти жертвы <br>
  Перед небом говорят: <br>
  "Цепи ржавые ислама <br>
  Наши братья сокрушат!" <br>
  <br>
  1877 <br>
  <br>
