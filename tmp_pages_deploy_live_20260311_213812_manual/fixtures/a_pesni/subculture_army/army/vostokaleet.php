Восток алеет, Китай вдали -<br>
Сотрём китайцев с лица земли!<br>
Моторы взвыли, Москва - прощай!<br>
А мы летим бомбить Китай!<br>
<br>
У нас две бомбы, покрытых лаком,<br>
На них одно лишь слово матом.<br>
Наш самолёт сверхзвуковой<br>
И лётчик я немолодой!<br>
<br>
Бомбил я Мюнхен, бомбил Берлин,<br>
Теперь лечу бомбить Пекин.<br>
И вот уже Пекин под нами -<br>
Жму на педаль двумя ногами!<br>
<br>
Кто был в Пекине - попали в рай,<br>
А мы летим бомбить Шанхай!<br>
Кто был в Шанхае - тех не вернуть,<br>
А я лечу в обратный путь!<br>
<br>
Наш самолёт уже без груза.<br>
Вдали видна черта Союза.<br>
Наш ждёт покой и отдых полный -<br>
Где был Китай - бушуют волны!<br>
<br>
И в Пентагоне вы знайте впрок,<br>
Судьба китайцев - вам урок!<br>
Не вздумайте тягаться с нами -<br>
Загоним в гроб двумя ногами!<br>
