<br><h1>ВОТ 
  КОРАБЛЬ, НАШ ГОСПОДИН </h1>
  <br>
  Вот корабль, наш господин, <br>
  Он с командой не один. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Господин наш капитан <br>
  Позволяет гулять нам. <br>
    <div class='refren'>Вот калина, вот малина! </div>
  Офицеры молодцы, <br>
  Как родные нам отцы. <br>
   <div class='refren'>Вот калина, вот малина! </div>
  Они пьют с нами, веселятся, <br>
  Не велят себя бояться. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Унтера все молодцы, <br>
  Скажем просто, удальцы. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Они в дудку засвистали, <br>
  Мы наверх все побежали. <br>
    <div class='refren'>Вот калина, вот малина! </div>
  Мы наверх все побежали, <br>
  Только тралы затрещали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Мы наверх все побежали, <br>
  Старшины нас поверяли. <br>
   <div class='refren'>Вот калина, вот малина! </div>
  Старшины нас поверяли, <br>
  Ближе к вантам посылали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Капитан сказал: к вантам! <br>
  Лейтенант кричит: по марсам! <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Мы по марсам побежали, <br>
  Только ванты задрожали. <br>
   <div class='refren'>Вот калина, вот малина! </div>
  Мы на марс все выбегали, <br>
  Лисер-спирты подымали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Лисер-спирты подымали, <br>
  Близко к топу мы вставали. <br>
    <div class='refren'>Вот калина, вот малина! </div>
  Близко к топу мы вставали <br>
  И команды ожидали. <br>
    <div class='refren'>Вот калина, вот малина! </div>
  Лейтенант кричит: по реям! <br>
  Старшина: с линьком скорее! <br>
    <div class='refren'>Вот калина, вот малина! </div>
  Мы по реям разбежались, <br>
  Ногами в перты упирались. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Марсель-брамсель отдавали, <br>
  С рей долой скоро сбежали. <br>
   <div class='refren'>Вот калина, вот малина! </div>
  С рей долой скоро сбежали, <br>
  Марса-шкоты натягали. <br>
   <div class='refren'>Вот калина, вот малина! </div>
  Марса-шкоты натягали, <br>
  Марса-фалы подымали. <br>
    <div class='refren'>Вот калина, вот малина! </div>
  Как на левом тоноке, <br>
  Там заел брасы в блоке. <br>
   <div class='refren'>Вот калина, вот малина! </div>
  Тут матросы суетятся, <br>
  По снастям все становятся. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Боцман в росторах стоит <br>
  И ничего не говорит. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Ничего не говорит, <br>
  Во все стороны глядит. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Во все стороны глядит, <br>
  В медну трубочку свистит. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  В медну дудочку свистит, <br>
  К себе рекрутов манит. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Беги, серы, поскорее, <br>
  Бери брасы посмелее! <br>
   <div class='refren'>Вот калина, вот малина! </div>
  Рей брасом поправляли, <br>
  Снасти в бухты укладали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Снасти в бухты укладали, <br>
  Нас на шпиль всех прогоняли. <br>
   <div class='refren'>Вот калина, вот малина! </div>
  Нас на шпиль всех прогоняли, <br>
  Ходом якорь доставали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Ходом якорь доставали, <br>
  На кат-фиш мы его брали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  На кат-фиш мы его брали, <br>
  На перпулин якорь клали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Прощай Ревель и форштадт, <br>
  Мы отправимся в Кронштадт. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Прощай Рижская гора, <br>
  Нам в Кронштадт идти пора. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Прощай Узкой переулок, <br>
  Где поели много булок. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Как в Кронштадт мы приходили, <br>
  Мы на якорь становились. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Якорь в воду отдавали, <br>
  Шлюпки на воду спускали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Шлюпки на воду спускали, <br>
  Нас гулять всех отпускали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  Мы немного погуляли, <br>
  На гауптвахту все попали. <br>
  <div class='refren'>Вот калина, вот малина! </div>
  <br>
  <span class='i'>Матросская шуточная песня, имевшая и сугубо практическое значение. В ней 
  перечисляются основные термины морских парусных кораблей: ванты, марсы, лисер-спирты, 
  топы, линьки, перты, марсель-брамсель, марса-шкот, марса-фал, тонок, ростор, 
  брасы, кат-фиш, перпулин и т. п. Всю эту "морскую азбуку" рекруты (в песне они 
  недаром называются "серыми") должны были вызубрить, знать наизусть, чему во 
  многом способствовала песня. Подобным же образом в былинах о Василии Буслаеве 
  новгородского богатыря с детства обучают не просто чтению и пению, а "четью-петью 
  церковному" - чтению и запоминанию церковных обрядов с помощью пения. Этот древнейший 
  обычай сохранялся и в армии. </em><br>
