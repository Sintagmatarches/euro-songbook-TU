        <br>
        Гуревич А.В., Элиасов Л.Е. Старый фольклор Прибайкалья. Т. 1. Улан-Удэ, 1939. С. 307. Раздел «Солдатские песни», №182.</span><br><br><br>
        Скорее всего, речь идет о русско-японской войне 1904-1906 гг. либо о подавлении Боксерского восстания 1899-1901 гг., которое в России называли &quot;Китайской войной&quot;. <br>

