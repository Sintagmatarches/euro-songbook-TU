        <h1>ВЫШЛО СОЛНЦЕ ИЗ ОКОНЦА...</h1><br>
      
        Вышло солнце из оконца<br>
        За зелененький садок.<br>
        - Пойдем, милка, погуляем<br>
        Хоть последний вечерок.<br>
        Лягу спать я на кровати,<br>
        Расскажу тебе я сон,<br>
        Сон про тебя, сон про меня,<br>
        Сон про нашую любовь.<br>
        Как мы жили, споживали,<br>
        Любовались красотой.<br>
        А теперь нас разлучают,<br>
        Разлучают нас с тобой.<br>
        Разлучает нас с тобой<br>
        Да распроклятая война,<br>
        Распроклятая война<br>
        Да чуже-дальние края.<br>
        - А тепере, милый мой,<br>
        Да не увидимся с тобой.<br> 
			<br>
