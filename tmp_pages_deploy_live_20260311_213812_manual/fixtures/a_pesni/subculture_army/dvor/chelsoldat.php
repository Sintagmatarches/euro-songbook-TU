<h1>СЮЗАННА</h1>
 
  Шел солдат из Алабамы<br>
  До своих родных краев,<br>
  До своей любимой мамы,<br>
  До сестер и до братьев.<br>
  <br>
  <em>Припев:</em><br>
  <br>
  Э-э-ге-гей, Сюзанна,<br>
  Любимая моя,<br>
  После долгих лет разлуки<br>
  Я пришел в твои края!<br>
  <br>
  И ручей там протекает,<br>
  И стоит там старый дом,<br>
  И собака громко лает<br>
  Возле дома под окном.<br>
  <br>
  Не ручей там, а клоака,<br>
  И разрушен старый дом,<br>
  И повешена собака<br>
  Возле дома под окном.<br>
  <br>
  Я бандитов Ку-клукс-клана<br>
  По делам их узнаю,<br>
  Отомщу за смерть Сюзанны<br>
  И за всю мою семью!<br>
  <br>
        <span class='i'>В нашу гавань заходили корабли. Пермь: Книга, 1996. Перепечатано: 
        В нашу гавань заходили корабли. Вып. 2. М.: Стрекоза, 2000. - оба раза 
        под загл. &quot;Шел солдат из Алабамы&quot;.</span><br>
<br>
  <br>
  Русская версия популярной американской песенки (в американском оригинале куплетов 
  про Ку-клукс-клан не было). Вообще, песенка интернациональная - она бытует на 
  разных языках. В частности, в Польше входит в традиционный репертуар харцеров 
  (польских скаутов) и моряков. Любопытно, что на русский язык песня переведена главным поэтическим размером русских армейских маршевых песен XIX - начала XX в.: четырехстопным хореем с чередованием мужских и женских рифм.<br>
  <br>
  Появилась не позднее начала 1950-х: упоминается в автобиографической повести 
  Михаила Бобовича (1935-1988) &quot;К северу от Вуоксы&quot;, где ее поют по 
  дороге студентки-филологи ЛГУ, отправленные в карельский колхоз на картошку 
  (время действия - октябрь 1953, 2-й курс). См. журнал &quot;Нева&quot;, 1998, 
  №10. Там же студентки поют еще одну интернациональную песню - <a href="../ww2/folk/tchietoserdce.htm">&quot;Чье-то 
  сердце загрустило...&quot;</a><br>
