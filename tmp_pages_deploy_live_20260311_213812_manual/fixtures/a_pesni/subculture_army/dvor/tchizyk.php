    <strong>Наум Синдаловский </strong><br>
    <br>
    <span class="header"><strong>ЖИТЬ БЫ НА ФОНТАНКЕ, НО С ВИДОМ НА МАНХЭТТЕН</strong><br>
    </span>
    <br>
	<em>Из книги Наума Синдаловского «На языке улиц. Рассказы о петербургской фразеологии». 
  М.: ЗАО Центрполиграф, 2006, с. 84-86.</em><br>
  <br>
  В первые годы существования Петербурга, как, впрочем, и в допетровские времена, 
    Фонтанка называлась Безымянным ериком. Ериками, то есть речными протоками, 
    образовавшимися при разливе, назывались многие небольшие речки, вытекающие 
    из одной большой реки и в нее же впадающие. Как известно, Фонтанка берет свое 
    начало в Неве, у Прачечного моста, пересекает город с севера на юго-запад 
    и впадает в Неву почти у самого ее устья. После того как через Безымянный 
    ерик были перекинуты трубы, питавшие водой из Лиговского канала фонтаны Летнего 
    сада, речку стали называть ее современным именем. С 1737 года это имя становится 
    официальным. Но уже во второй половине XVIII века в фольклоре наряду с просторечным: 
    «Фонталка», появляется параллельное название: «Малая Нева». <br>
    <br>
    Надо сказать, что одним из самых любимых объектов городского фольклора Фонтанка 
    оставалась на протяжении всей истории Петербурга. Широко известны за пределами 
    Петербурга знаменитые частушки о незадачливом «Чижике-пыжике», как называли 
    в Петербурге кадетов Училища правоведения за форменные мундиры желто-зеленого 
    цвета: <br>
    <br>
    <em>Чижик-пыжик, где ты был? — <br>
  На Фонтанке водку пил. <br>
  Выпил рюмку, выпил две, <br>
  Зашумело в голове. </em><br>
  <br>
  Судьба одного из подгулявших воспитанников Училища правоведения до сих пор 
    волнует городской фольклор. Правда, теперь уже в связи с экологическими проблемами: <br>
    <br>
    <em>Чижик-пыжик вместо пьянки <br>
  Выпил воду из Фонтанки. <br>
  Видно, градусы не те: <br>
  Зашумело в животе. <br>
  <br>
  Чижик-пыжик после пьянки <br>
  Похмелился иэ Фонтанки. <br>
  Откачали эту птицу <br>
  Только в Боткинской больнице. </em><br>
  <br>
  Значительное место уделено Фонтанке и в петербургской фразеологии: «С тобой 
    спорить только у Фонтанки», «Ну и ты не дешевле Фонтанки», «На Фонтанке треснул 
    лед — в гости корюшка плывет». А в одной из пословиц заключена петербургская 
    формула вечной интеллигентской душевной раздвоенности, когда и Петербург дорог 
    до слез, и унестись хотелось бы куда-то, где деревья более аккуратно подстрижены 
    и газоны менее вытоптаны, да и сам Петербург мог бы быть другим, более благоустроенным 
    и приспособленным для человека. Короче: «Жить бы на Фонтанке, но... с видом 
    на Манхэттен». Впрочем, ностальгирующие по родине выходцы из Петербурга, в 
    разное время и по разным причинам переселившиеся на постоянное место жительства 
    в Америку, охотно пользуются другим вариантом той же самой поговорки: «Жить 
    бы на Манхэттене, но... с видом на Фонтанку». <br>
