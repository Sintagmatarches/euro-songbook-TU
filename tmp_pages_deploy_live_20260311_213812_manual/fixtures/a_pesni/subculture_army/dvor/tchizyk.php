	<span class="header"><strong>ЧИЖИК-ПЫЖИК</strong> </span>
  <br>
  <br>
  - Чижик-пыжик, где ты был?<br>
  - На Фонтанке водку пил!<br>
  Выпил рюмку, выпил две -<br>
  Закружилось в голове!<br>
  <br>
  <img src="tchizyk.gif" width="665" height="90"><br>
  <br>
  <br>
  Куплет, который обычно связывают с воспитанниками петербургского Училища правоведения, располагавшегося на 
  набережной Фонтанки, дом 6. Это было закрытое учебное заведение для детей дворян, 
  основанное в 1838 году по предложению сановника Михаила Сперанского, автора гражданских реформ Александра I. Кадеты училища носили мундиры 
  желто-зеленого цвета, за что их якобы прозвали &quot;чижиками-пыжиками&quot;. Если версия верна, то куплет возник не ранее 1838 года. При этом первые две строчки цитируются в повести Дмитрия Григоровича &quot;Капельмейстер Сусликов&quot;, написанной в 1848 году и в том же году опубликованной в 12-м номере &quot;Современника&quot;. Действие происходит в провинциальном городе Б*** &quot;несколько лет тому назад&quot; (впрочем, сам Григорович жил в Петербурге), песню поет шут в доме богатого городского обывателя:<br>
<br>
<em>Чижик, чижик, где ты был?<br>
На Фонтанке воду пил... и т.д.<br></em>
<br>
То есть, в 1840-е годы песенка уже была популярной.<br>
<br>
 Несколько воспитанников Училища правоведения были причастны к кружку вольнодумцев Петрашевцев (1845-49), и после процесса Петрашевцев (1849) 
  режим в училище был ужесточен, чтобы подобная революционная ситуация 
  не повторилась.<br>
  <br>
  Тем не менее, из стен Училища правоведения вышли многие 
  деятели культуры: композиторы Петр Чайковский и Александр Серов, литературный 
  и музыкальный критик Владимир Стасов, поэты Алексей Апухтин (<a href="../romans/donaurov/2gnedyh.php">&quot;Пара 
  гнедых&quot;</a>, <a href="../romans/spiro/notchibezum.php">&quot;Ночи безумные&quot;</a>), 
  Алексей Жемчужников (&quot;Осенние журавли&quot; - впоследствии известная андеграундная 
  песня <a href="zuravlinadkolymoj.php">&quot;Журавли&quot;</a>) и другие.<br>
  <br>
  Сейчас напротив здания бывшего училища у кромки воды Фонтанки установлен памятник 
  чижику-пыжику в натуральную величину. Ему бросают монетки на счатье. Памятник 
  часто воруют, но на его месте ставят новый.<br>
  <br>
  Фольклорист Владимир Бахтин сомневался, что песня связана со студентами. Он цитировал десять строк песни и проводил параллели со старой деревенской песней про зайчика:<br>
  <br>
  «Чижик-пыжик, где ты был?<br>
  На Фонтанке воду пил.<br>
  Выпил рюмку, выпил две,<br>
  Зашумело в голове.<br>
  Стали чижика ловить,<br>
  Чтобы в клетку посадить.<br>
  Чу, чу, чу, чу,<br>
  Я из клетки улечу.<br>
  Чижик в клетку не хотел,<br>
  Взял вспорхнул и улетел.<br>
	<br>
	Обычно знают только первые четыре строчки и считают, что они относятся к студентам училища правоведения, где Чайковский учился, но я не очень в этом убежден, потому что есть старая деревенская песенка &quot;Зайчик, зайчик, где ты был? В лесику, в лесику&quot;*»<br>
	<br>
	<em>* Пьющим водку на Фонтанке герой этой песенки вполне мог стать благодаря шутливому тосту:<br>
	<br>
	Едет чижик в лодочке<br>
	  В адмиральском чине...<br>
	  Не выпить ли водочки<br>
	  По такой причине? - Примеч. ред.<br>
        <br>
(Жили-были... Разговоры с В.С. Бахтиным / Сост. О.А. Комарова, СПб.: Композитор • Санкт-Петербург, 2006, с. 101. Редактор книги - А.Е. Моносов. Разговоры записаны в последние годы жизни Бахтина. Умер он в 2001 году.)<br>
    </em><br>
