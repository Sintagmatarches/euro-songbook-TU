	<span class="header"><strong>ТОВАРИЩ 
ЗАММИНИСТРА</strong><br></span>
<br>
Алло, алло! Кто там у аппарата?<br>
Вас вызывают из Москвы!<br>
Скажите мне, когда на зависть НАТО<br>
Заказ сдадите флоту вы?<br>
<br>
Как протекает перестройка?<br>
Искоренилась ли попойка?<br>
И не нужна ль кому головомойка,<br>
А также мойка головы?<br>
<br>
- Всё хорошо, товарищ замминистра!<br>
Дела идут, как никогда.<br>
У нас тут спирта целая канистра!<br>
Всё остальное - ерунда.<br>
<br>
Ах, впрочем, есть такая малость -<br>
У нас отвёртка поломалась.<br>
А в остальном, товарищ замминистра -<br>
Всё хорошо, как никогда!<br>
<br>
- Алло, алло! Мне, право, неудобно...<br>
Мне на отвёртку наплевать.<br>
Но я прошу докладывать подробно,<br>
Как вы могли её сломать?<br>
<br>
- Отвёртка - так, пустое дело.<br>
Упала в щит и там сгорела.<br>
А в остальном, товарищ замминистра,<br>
Всё хорошо, всё хорошо!<br>
<br>
- Алло, алло! Мне что-то непонятно...<br>
Чем больше дров, тем дальше в лес.<br>
Но я прошу докладывать мне внятно,<br>
Зачем электрик в щит полез?<br>
<br>
- Разъединить хотел контакты,<br>
Когда вразнос пошёл реактор.<br>
А в остальном, товарищ замминистра,<br>
Всё хорошо, всё хорошо!<br>
<br>
- Алло, алло! Мне что-то стало дурно.<br>
Куда девался валидол?<br>
Но я прошу пока ещё культурно<br>
Сказать мне, кто куда пошёл.<br>
<br>
- Пошла на дно корма заказа.<br>
Довольно медленно, не сразу.<br>
А в остальном, товарищ замминистра,<br>
Все хорошо, все хорошо!<br>
<br>
- Алло, алло! Кошмар! Мутится разум!<br>
Тудыть твою и растудыть!<br>
Я вас прошу докладывать мне сразу:<br>
Что там имело место быть?<br>
<br>
- Приняв на борт боезапас<br>
Пустился в плаванье заказ.<br>
Был бы прочнее старый таз -<br>
Длиннее был бы мой рассказ.<br>
Едва дала ракета газ -<br>
Экран локатора погас.<br>
Мы навели её "на глаз",<br>
И потопили наш баркас.<br>
Нас капитан обматерил<br>
И с огорченья закурил.<br>
Окурок в шахту зашвырнул.<br>
Тут арсенал весь долбанул.<br>
Водой наполнились трюма,<br>
На дно отправилась корма,<br>
Но на плаву остался нос -<br>
Реактор, стало быть, - вразнос.<br>
Электрик в щит полез - герой -<br>
Отвёртку вышибло искрой.<br>
Толпа спасается на плот -<br>
Сейчас реактор долбанёт!<br>
А мы сидим, нам - ни фига,<br>
До базы НАТО - два шага...<br>
А в остальном, товарищ замминистра,<br>
Всё хорошо, как никогда!<br>
