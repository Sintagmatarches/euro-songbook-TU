  <br></span>
        <span class="header"><strong>АНТИБОЛЬШЕВИЦКОЕ 
        ДВИЖЕНИЕ В ИРКУТСКОМ КАЗАЧЬЕМ ВОЙСКЕ. КРАТКИЙ ИСТОРИЧЕСКИЙ ОЧЕРК<br>
        </strong></span> <span class="text"><br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 265-267</em>.<br>
        <br>
        <br>
        История казачьего населения Иркутской губернии такова. В 1851 г. иркутские 
        городовые казаки, станичные казаки Иркутского и Нижнеудинского округов, 
        казаки Тункинского отдела Китайской пограничной линии были реорганизованы 
        в Иркутский конный полк. В 1871 г. полк свернули в сотню, а часть казаков 
        обратили в крестьянское сословие. <br>
        <br>
        На 1917 г. в селах Иркутской губернии было 941 казачье хозяйство с 5350 
        казаков: Иркутский уезд - 3193 казака (591 хозяйство), Киренский — 311 
        (58), Балаганский — 11 (2), Верхоленский — 7 (2), Черемховский — 859 (130), 
        Нижнеудинский — 969 (158). С учетом проживавших в Иркутске, можно полагать, 
        что собственно иркутских казаков было не более 7500 человек (или 1% населения 
        губернии), из которых 425 служили в армии. Кроме того, в Иркутской губернии 
        проживало 7350 «крестьян из казаков» (1295 хозяйств). Так как до 1917 
        г. 965 призывников этой категории служили не в казачьих частях, казаками 
        их считать нельзя. Однако, после 1917 г. численность создаваемого Иркутского 
        казачьего войска указывалась от 12000 до 15000 человек, явно с учетом 
        последней категории. <br>
        <br>
        До Гражданской войны из-за малочисленности иркутские казаки не имели статуса 
        отдельного войска, комплектуя трехсотенный конный дивизион (336 человек 
        при 2 пулеметах) в Иркутске и 1 сотню Красноярского казачьего Дивизиона 
        в Нижнеудинске (85 человек). Во время Первой мировой войны Иркутский казачий 
        дивизион на фронте не был, подавляя волнения солдат запасных полков в 
        Ачинске, Минусинске, Красноярске и Иркутске. После Февральской революции 
        атаманом иркутских казаков был избран генерал-майор П.П. Оглоблин (1872-1940). 
        <br>
        <br>
        Иркутские казаки участвовали в боях 21-30 декабря 1917 г. в Иркутске между 
        большевицкими силами (солдаты запасных частей и рабочие-красногвардейцы) 
        и юнкерами, на стороне последних. 18 декабря большевики предписали всем 
        юнкерам и казакам сдать оружие. В ответ представители юнкеров потребовали 
        производства в офицеры, денег на обмундирование и отправку в полки. Большевики 
        заявили, что полков не существует, а звания упразднены. В полдень 21 декабря 
        юнкера под руководством полковника Д.Г. Лисученко начали занимать близлежащие 
        к их расположению дома и кварталы. Выступившие под руководством атамана 
        генерал-майора П.П. Оглоблина казаки заняли Духовную семинарию и детскую 
        больницу. Казачья разведка приводила целые партии пленных. Большевики 
        же приступили к артиллерийскому обстрелу зданий, занятых юнкерами и казаками. 
        Упорные бои развернулись в центре Иркутска, где атакам юнкеров подвергался 
        понтонный мост через Ангару и Белый дом. Управляющий французским консульским 
        агентством Жандро писал: «С одной стороны юнкера, казаки, с другой - большевики 
        вели ожесточенную борьбу, заставлявшую содрогаться от ужаса даже тех, 
        кто провел три года на фронте... У большевиков 6000 человек, у юнкеров 
        600... Эсеры, обещавшие содействие юнкерам, попрятались на все дни крови 
        и огня и вылезли после бури». С 23 декабря в Иркутск на помощь большевикам 
        стали прибывать отряды из Черемхово, Красноярска, Ачинска, Канска и т.д. 
        В Иркутске полыхали пожары от артиллерии красных и поджогов солдат, красногвардейцы 
        грабили жителей. <br>
        <br>
        В ночь на 25 декабря три казачьи сотни под началом есаула Селиванова, 
        подъесаулов Кубинцева и Коршунова выступили на помощь юнкерам. По дороге 
        у Входо-Иерусалимской церкви казаки имели стычку с красными, в которой 
        с обеих сторон погибло около 10 человек. К рассвету казаки подошли к военному 
        училищу, доставив 6 подвод хлеба и патронов. В полдень 25 декабря казаки 
        атаковали 2-й комиссариат милиции, где засели красные, и в 14 часов захватили 
        его, пленив 30 человек. Разоружив всех, часть пленных казаки распустили. 
        К концу боев в казармах казаков находилось «более 1000 пленных, кормить 
        их нечем, предлагают им уходить, желающих нет, предпочитают оставаться 
        под охраной казаков». По данным красных, казаки участвовали в атаках Белого 
        дома — главного оплота большевиков в Иркутске, захваченного юнкерами в 
        ночь на 29 декабря с пленением 153 защитников. Утром 30 декабря стороны 
        подписали договор, по которому власть в Иркутске и губернии передавалась 
        губернскому совету из представителей Советов, городской думы, земства 
        и профсоюзов. Красные так и не выполнили договор, а 4 января аннулировали 
        его. <br>
        <br>
        По неполным данным, в декабрьских боях 1917 г. в Иркутске было убито 229 
        солдат и красногвардейцев, 65 гражданских лиц, 38 юнкеров, в том числе 
        забайкальский казак И.Г. Лоскутников, 9 офицеров, 8 казаков: Ф.Н. Балышев, 
        М.К. Беленушкин, С.В. Воронин, Ф.Ф. Дожкин, И.И. Кошкаров, И.И. Распутин, 
        М.М. Фереферов, И.И. Воротников (последний — казак Нижнеудинского поселения, 
        остальные Иркутского). Общее число убитых, замерзших, утонувших в Ангаре, 
        раненых составило более 1000 человек. Итоги боев оказались обескураживающими 
        для белых. Их подвела политическая неискушенность и незнание методов врага. 
        Выступление юнкеров стало реакцией на действия, на практику и идеологию 
        шедших к власти большевиков. Руководители восстания и рядовые юнкера продолжили 
        борьбу, быстро избавляясь от благодушия и политической наивности по отношению 
        к большевикам и эсерам. Генерал-майор П.П. Оглоблин был вынужден уехать 
        во Владивосток, в марте 1918 г. в Харбине вступил в Особый Маньчжурский 
        отряд (ОМО) есаула Г.М. Семенова начальником снабжения и тыла, с 3 августа 
        был временно исполняющим обязанности начальника штаба. Вместо Оглоблина 
        атаманом в феврале 1918 г. 2-м Войсковым Кругом был избран есаул С.А. 
        Лукин. <br>
        <br>
        Борьба в Иркутской губернии в первой половине 1918 г. приняла характер 
        подпольного противостояния, а также отдельных выступлений против большевиков. 
        Местные белые силы постепенно усиливались, домой возвращались офицеры-фронтовики. 
        Часть офицеров пополнила антибольшевицкое подполье, возглавляемое полковником 
        А.В. Эллерц-Усовым. Структуры подполья существовали в Иркутске, Черемхово, 
        Усолье, Балаганске, Киренске, Нижнеудинске. Среди взводных командиров 
        Иркутского подполья хорунжий А. Смирнов. <br>
        <br>
        Нижнеудинская подпольная организация под руководством подъесаула Г.В. 
        Кузнецова и бывшего члена 4-й Государственной Думы эсера И.Н. Манькова 
        опиралась на живших в Куйтунской волости казаков и насчитывала 92 человека. 
        Среди подпольщиков подъесаул А. Козловский, сотник А.С. Трофимов, хорунжий 
        И.В. Алистратов, урядник Е. Аболаков, казаки П. Борзов, А. Бутырин, В. 
        Гладких, И. Домницкий, В. Жилинский, Н. Кузнецкин, К. Овчинников, В. Ошаров, 
        В. Ремезов. <br>
        <br>
        Решающий этап противоборства в Восточной Сибири начался с выступлением 
        Чехословацкого корпуса. 26 мая 1918 г. большевики в Иркутске неудачно 
        пытались разоружить три чешских эшелона. В ответ 28 мая около 1000 чехов 
        трех других эшелонов во главе с подполковником Б.Ф. Ушаковым восстали 
        в Канске и Нижнеудинске. Действия Чехословацкого корпуса позволили белым 
        выйти из подполья, сформировать Сибирскую армию и начать вооруженную борьбу 
        с большевиками. В ночь на 29 мая подпольная организация свергла Советскую 
        власть в Нижнеудинске, белые арестовали до 100 человек и расстреляли несколько 
        членов местного Совета. В дальнейшем 70 бывших подпольщиков при поддержке 
        чехов вели бои с наступавшими от Тулуна войсками Центросибири. Белые сумели 
        продержаться до прибытия под- креплений из Западной Сибири. <br>
        <br>
        В ночь на 14 июня иркутские подпольщики подняли восстание, чтобы задержать 
        отряды большевиков и тем дать возможность нижнеудинцам продержаться до 
        подхода чехов, а, по возможности, и свергнуть власть большевиков. Восставшие 
        захватили губернскую тюрьму и выпустили около 160 заключенных. Часть освобожденных 
        присоединилась к белым, попытавшимся захватить ключевые пункты Иркутска. 
        В результате ночного боя с красноармейцами и рабочими восставшие были 
        рассеяны. В бою убито 14 красногвардейцев и 9 гражданских лиц. Большевики 
        расстреляли 11 человек, в том числе есаула И. Тюменцева, арестовали до 
        140 человек, из которых 108 этапировали в Читу, в том числе есаула Селиванова 
        и прапорщика Алексеева. <br>
        <br>
        24-26 июня восточнее Нижнеудинска произошло упорное сражение с участием 
        бронепоездов, артиллерии и не менее 2000 бойцов с каждой стороны. Его 
        исход решился, когда небольшой конный отряд белых с помощью местных казаков 
        в ночь на 26 июня вышел к разъезду Шеберта и изрубил 400 красногвардейцев 
        с Касьяновской шахты, «которые поужинали с выпивкой и заснули, не выставив 
        охраны». Красные начали отступать. 11 июля белые заняли Иркутск, 20 августа 
        <br>
        Верхнеудинск. В вооруженной борьбе июля-августа 1918 г. антибольшевицкие 
        силы (с 21 июля — Восточный фронт) в составе Средне-Сибирского корпуса 
        под началом полковника А.Н. Пепеляева и «Восточного отряда чехословацких 
        войск» генерал-майора Р.И. Гайды заняли территорию Восточной Сибири. Несмотря 
        на превосходство красных в живой силе и технике, белые в ходе ряда операций 
        на окружение на южном побережье Байкала нанесли красным полное поражение. 
        На стороне белых действовали 3-я сотня Енисейских казаков и отдельная 
        Нижнеудинская сотня. Командование дало ей такую оценку: «Вы, славные Нижнеудинские 
        казаки, одними из первых свергли позорную большевицкую власть в Нижнеудинске 
        и встали в ряды молодой Сибирской Армии». <br>
        <br>
        После разгрома красных в Восточной Сибири, белые части были отправлены 
        на Урал, где продолжили борьбу с Красной армией. Это привело к тому, что 
        в ряде районов часть красных отрядов сумела уцелеть и через полгода развернуть 
        партизанское движение. С занятием Восточной Сибири вооруженные силы Временного 
        Сибирского правительства получили обширный тыл, в котором можно было пополнить 
        старые и сформировать новые части и соединения.<br>
        <br>
        Войсковой атаман иркутских казаков есаул С.А. Лукин 24 июля 1918 г. мобилизовал 
        всех офицеров, врачей, военных чиновников и казаков призыва 1916-1919 
        гг., то есть 1899-1896 гг. рождения. По распоряжению атамана, «казаки 
        должны явиться немедленно в зависимости от расстояния, с конем, снаряжением 
        и вооружением... В сформированных казачьих частях устанавливается твердая 
        дисциплина. Выборное начало и комитеты упраздняются». В Иркутске началось 
        формирование пешей и конной казачьих частей. Общее командование ими принял 
        войсковой старшина Могилев, его помощниками стали есаул Емельянов и хорунжий 
        А.В. Балашов. 13 августа среди казачьего населения Иркутской губернии 
        началась мобилизация лиц от 18 до 25 лет, имеющих высшее и среднее образование, 
        прослушавших 4 класса средних учебных заведений, городских, высших начальных 
        и специальных училищ, получивших права вольноопределяющихся 2-го разряда. 
        Уже отслужившие казаки старше 23 лет от призыва освобождались. Командующий 
        (с 11 июля) Иркутским военным округом полковник А.В. Эллерц-Усов 17 августа 
        выступил за призыв в казачьи части бурятского населения «как весьма зажиточного, 
        в массе чуждого большевизму», но Временное Сибирское правительство не 
        поддержало эту инициативу. <br>
        <br>
        В Иркутске 20 сентября 1918 г. началось формирование 4-й Иркутской конной 
        бригады, которую с 8 ноября возглавил генерал-мйор И.Ф. Шильников. В бригаду 
        вошли Иркутский казачий (полковник П.И. Войлошников) и Иркутский гусарский 
        (войсковой старшина Васильев) полки. Казачий полк, по ходатайству казаков, 
        развернулся из дивизиона. На 1 октября 1918 г. в Иркутском казачьем полку 
        насчитывалось 102 офицера и 380 казаков, на 5 ноября - 109 офицеров и 
        387 казаков. Из офицеров Иркутского казачьего полка только 15 занимали 
        командные должности. Иркутский гусарский полк из-за малочисленности был 
        преобразован в кавалерийский дивизион Партизанской (Егерской) бригады 
        имени есаула Красильникова. Взамен в 4-ю бригаду 12 декабря был включен 
        Енисейский казачий полк (хорунжий Г.К. Бологов). К концу декабря 1918 
        г. 4-я Иркутская конная бригада насчитывала 800 человек. <br>
        <br>
        17 февраля 1919 г. 2-й Забайкальский казачий полк, по приказу командующего 
        (с 25 декабря 1918 г.) Иркутским военным округом генерал-майора В.И. Волкова, 
        был передислоцирован из Троицкосавска в Иркутск и включен в 4-ю Иркутскую 
        конную бригаду, которая 10 мая переименована в отдельную Сводно-казачью. 
        18 февраля В.И. Волкова сменил генерал-лейтенант В.В. Артемьев. <br>
        <br>
        Драматичные события произошли в начале октября 1918 г. в Тункинской долине, 
        где появился Красный отряд анархиста Н.А. Каландаришвили, насчитывавший 
        до 1200 бойцов при 12 пулеметах. После крушения Прибайкальского фронта, 
        красные двинулись из района Троицкосавска на запад, на соединение с наступающей, 
        по их мнению, Красной армией. Пройдя вдоль реки Джида, они у поселка Модонкуль 
        перешли в Монголию, где провели две недели. Затем, не зная обстановки, 
        попытались пробиться через с. Туран на с. Шимки и далее к Транссибирской 
        железной дороге. Проживавшие в Тункинской долине казаки под руководством 
        станичных атаманов Зверева и Бобкова организовали отпор, на помощь из 
        Иркутска прибыл отряд сотника И.К. Скуратова из 64 бойцов. Казаки уничтожили 
        или рассеяли большую часть красного отряда, отбросив остальных к истоку 
        р. Ока. Был захвачен адъютант Н.А. Каландаришвили с 280 тысячами рублей, 
        награбленными в Иркутске. Прошения казаков передать деньги на возмещение 
        причиненного анархистами ущерба были отклонены. Рассеянные красные были 
        пленены или, перейдя на положение мирных жителей, вернулись в родные места. 
        Около 300 отброшенных в Саяны красных вдоль рек Ока и Большая Белая вышли 
        в деревни Чернушка и Инга, где были пленены отрядом полковника И.Н. Красильникова. 
        <br>
        <br>
        В течение 1919 г. подразделения Иркутского казачьего полка участвовали 
        в операциях против красных партизан в районах городов Нижнеудинск Иркутской 
        губернии и Канск Енисейской губернии. В августе все иркутские казаки, 
        способные носить оружие, были призваны в армию. 5 августа 1919 г. штаб 
        и 2 сотня Иркутского казачьего полка с пулеметной и учебной командами 
        получили распоряжение отправиться из Иркутска на соединение с 3 сотней 
        полка, находившейся южнее участка железной дороги Свище — Канск. Одновременно 
        1 сотне полка, расквартированной в Черемхово, было приказано отправиться 
        на станцию Тайшет в распоряжение командира Румынского легиона полковника 
        Э. Кадлеца. <br>
        <br>
        К 1 ноября 1919 г., включавший 4 сотни и пулеметную команду Иркутский 
        казачий полк насчитывал 61 офицера, 508 казаков при 12 пулеметах, а к 
        20 ноября - 44 офицера, 625 казаков при 13 пулеметах. Когда в конце ноября 
        произошли волнения 70 солдат гарнизона Балаганска, во избежание повторения 
        белые разместили в городе сотню Иркутского казачьего полка под началом 
        хорунжего Соломатова. В конце 1919 г. — начале 1920 г. Иркутский казачий 
        полк составлял гарнизон Канска, где после поражения белых и рассеялся.<br>
        <br>
        Оставшиеся в Иркутске учебная и нестроевая команды Иркутского казачьего 
        полка 4 января 1920 г. поддержали мятеж эсеровского Политцентра против 
        власти А.В. Колчака и под руководством П.П. Оглоблина пленили оренбургских 
        юнкеров. Ранее П.П. Оглоблин потребовал от колчаковского правительства 
        «сказать, наконец правду, кто придет на помощь и какова будет эта помощь» 
        или «казачество поступит так, как подсказывает ему совесть и как диктуют 
        интересы». На стороне Политцентра с 28 декабря 1919 г. действовала и отдельная 
        конная сотня есаула М.Х. Петелина. <br>
        <br>
        23 января 1920 г. в Иркутске власть перешла к большевикам, а оставшиеся 
        в городе казаки образовали 1-й Иркутский Советский казачий полк во главе 
        с Бутаковым. 11 февраля этот полк преградил каппелевцам дорогу от Иркутска 
        на Култук, а в конце февраля в составе 210 казаков выступил в Забайкалье 
        на борьбу с белыми. В бою под селами Улятуйское и Бальзой 1 июня 2 офицера, 
        11 юнкеров и 62 казака бывшего Иркутского казачьего полка во главе с подъесаулом 
        Петровым перешли на сторону белых. <br>
        <br>
        2 апреля 1920 г. большевики объявили казачьи земли Иркутской губернии 
        государственными, Иркутское казачье войско прекратило существование на 
        родной земле. Оставшиеся на родине казаки активно участвовали в повстанческом 
        движении 1920-х гг. Среди руководителей повстанческих отрядов есаул С.А. 
        Лукин и хорунжий А.И. Шубин в Тункинской долине и Монголии, хорунжий Ермаков 
        у с. Голуметь, есаул С. Мамаев у с. Тайшет, вахмистр И.Г. Сенотрусов у 
        с. Кимельтей и многие другие. В Харбине в 1934-1935 гг. Зарубежная станица 
        Иркутского казачьего войска, возглавляемая С.К. Малых, издавала альманах 
        «Иркутский казак». <br>
