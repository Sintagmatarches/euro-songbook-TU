  <strong>Пилюк Моисей (в просторечии — Мусий) Прокофьевич</strong> — казак ст. 
  Елизаветинской, середняк. Сотник, член Кубанской краевой рады в 1919-1920 гг. 
  Сторонник «самостийности» Кубани, симпатизировал эсерам. В конце 1919 г. возглавил 
  массовое восстание казаков-черноморцев против Деникина. С приходом большевиков 
  к власти поддержал их, в июле 1920 г. назначен председателем комиссии Кубано-Черноморского 
  облревкома по борьбе с «бело- зелеными». В сентябре 1920 — январе 1921 гг. — 
  фактический руководитель казачьей секции областного ревкома. В январе 1921 г. 
  избран кандидатом в члены облисполкома Советов. Убедившись в преднамеренности 
  «расказачивания», в январе 1921 г. бежал с семьей в горы, где возглавил Политотдел 
  Кубанской повстанческой армии. Считался идеологом КПА. В октябре 1921 г. пойман, 
  осужден. После тюремного заключения вернулся в ст. Елизаветинскую психически 
  больным. (ГАКК. Ф. Р-158. Оп. 1. Д. 160. Л. 224-225; Берлизов А. Е. Темные борцы 
  за народное счастье //Комсомолец Кубани. 1988. 28.11.).<br>
