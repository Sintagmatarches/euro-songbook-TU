  артиллерийское училище и артиллерийская академия, Николаевская академия. Военная 
  служба с 1876 г., генерал от инфантерии (с 1916 г.). Участник боев на Кавказском 
  фронте 1-й мировой войны в чине генерал- майора. Георгиевский кавалер 3-й и 
  4-й степеней. В июне-декабре 1917 г. — командующий Кавказским фронтом. В 1918-1919 
  гг. — руководил отрядом Добровольческой армии в Баку, затем в Порт-Петровске 
  (Махачкала). С 1920 г. участник «бело-зеленого» движения. В эмиграции — в Югославии. 
