	<br><span class="text"><strong>А. В. Баранов</strong><br>
  <br>
        <span class="header"><strong>ПОВСТАНЧЕСКОЕ ДВИЖЕНИЕ «БЕЛО-ЗЕЛЕНЫХ» 
        В КАЗАЧЬИХ ОБЛАСТЯХ ЮГА РОССИИ (1920-1924 гг.)</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 119-129</em>.<br>
  <br>
  <br>
  В начале 1920-х гг. Россия переживала глубокий кризис государственности. Утрата 
  традиционного смысла бытия вызывала раскол общества и «борьбу всех против всех», 
  порождала сепаратизм окраин. Исследование повстанческого движения на казачьем 
  Юге России (1921-1924 гг.) позволит выявить причины и факторы конфликтов и развенчать 
  миф о покорности общества времен НЭПа перед лицом власти и предостеречь политиков 
  от насилия. <br>
  <br>
  Исторические работы о повстанчестве Юга России многочисленны, но не дают системного 
  знания. Советские авторы оценивали это движение как форму сословной и классовой 
  борьбы, как «политический бандитизм» мелкой буржуазии. Относительная свобода 
  суждений проявлялась только в работах 1920-х гг. А. Козаков, Стройло, Е. Шейдеман 
  и другие признавали «насажденность» власти РКП(б) в казачьих областях и ее непопулярность. 
  Авторы 1920-х гг. отличали стихийное сопротивление большинства казаков «военному 
  коммунизму» от уголовщины, а, тем более, от Белого движения. Восстания 1921-1924 
  гг. оценивались как продолжение Гражданской войны на «внутреннем фронте». Давался 
  сравнительно объективный анализ военных действий с цитированием документов обеих 
  сторон<sup>1</sup>. Мы впервые изучили трактовку повстанчества в трудах авторов 
  российского зарубежья — П.Н. Милюкова, С.С. Маслова, П.Л. Макаренко и другие.<sup>2</sup> 
  Они справедливо осуждают большевицкий режим и его репрессивные меры. Но для 
  нас неприемлем сепаратизм «самостийного» течения казачества (П.Л. Макаренко, 
  И.А. Билого, Г.В. Губарева и проч.)<sup>3</sup>. Подходить к выбору между единством 
  России или распадом на этносословные мини-государства следует с державных позиций. 
  <br>
  <br>
  Советские историки 1960-80-х гг. возобновили изучение «бело-зеленого» движения 
  на более широкой источниковой базе. Можно отметить работы И.Я. Трифонова, Б.И. 
  Степаненко, Н.Ф. Бугая, И.Я. Куценко<sup>4</sup>. Но и в этот период цензурные 
  и идеологические ограничения не позволяли создать научно обоснованные концепции. 
  <br>
  <br>
  Становление данных концепций происходит с 1988 г. по сей день в условиях плюрализма. 
  Трудно переоценить значение первых публикаций, свободных от официоза — статей 
  А.Е. Берлизова, А.И. Кругова<sup>5</sup>. Более системное и аргументированное 
  осмысление темы происходит в историографии второй половины 1990-х гг. (А.В. 
  Венков, А.В. Баранов, П.Г. Чернопицкий, И.В. Яблочкина и другие)<sup>6</sup>. 
  <br>
  <br>
  Современные исследователи обосновывают точку зрения, по которой крестьянские 
  и казачьи восстания были одной из форм Гражданской войны, не менее важной, чем 
  противоборство регулярных армий красных и белых. Чаще всего употребляются понятия 
  «крестьянская война» (В.П. Данилов) и «малая гражданская война» (А.А. Зайцев, 
  С.А. Павлюченков, В.Н. Бровкин<sup>7</sup>) как синонимы событий начала 1920-х 
  гг. В.П. Данилов обосновал гипотезу о крестьянской революции 1902—1922 гг. в 
  России. Крестьяне, потерпев военное поражение, одержали политическую победу, 
  т.е. оборвали военно-коммунистический эксперимент и закрепили свои права в Земельном 
  кодексе<sup>8</sup>. Мы считаем плодотворным историко-антропологический подход 
  к исследованию протеста, обоснованный З.А. Чеканцевой<sup>9</sup>. Вслед за 
  школой «Анналов» Чеканцева трактует крестьянские стихийные движения как явление 
  массовой культуры и обычного права. Бунт рассматривается как способ выражения 
  местной солидарности против «чужаков», как ответ общинной деревни на агрессию 
  внешнего (модернизируемого) мира. Обоснование бунта — представления о справедливом 
  распределении товаров, о покровительственной (но не диктаторской!) роли власти, 
  «этика выживания» земледельцев. Народные движения компенсируют утрату или слабость 
  государственных воздействий на общество во время кризисов. <br>
  <br>
  Источниковая основа темы быстро обновляется. Долгие годы использовались документы 
  только одной, советской стороны: законодательные акты, пропагандистские материалы, 
  оперативная документация, воспоминания полководцев. С конца 1980-х гг. интенсивно 
  исследуются ранее засекреченные сводки и обзоры ВЧК-ОГПУ, устные высказывания 
  и частная переписка противников власти, мемуары повстанцев<sup>10</sup>. <br>
  <br>
  Вооруженная оппозиция на Северном Кавказе в 1921-1924 гг. представляла собой 
  пестрый спектр политических сил, конфликтующих между собой — от монархистов 
  до анархистов. Затяжная война деморализовала многих своих участников и обесценила 
  жизнь. Но неверно видеть вслед за советскими историками во всех «зеленоармейцах» 
  преступников, пренебрегать разнородностью и сложными передвижками общественных 
  сил в повстанчестве. Повод для размышлений дает вывод политработника 1-й Конной 
  армии Стройло (осень 1921 г.): «Чистый бандитизм — свойство очень немногих мелких 
  отрядов, не имеющих ничего общего с крупными политическими организациями». Их 
  предводители принимали все меры к искоренению грабежей и даже «достигли большого 
  успеха» в этом<sup>11</sup>. <br>
  <br>
  Социальный состав «бело-зеленых» на основе доступных источников можно оценить 
  только описательно. Обычно во главе отрядов стояли офицеры или казаки. Было 
  много бывших солдат Добровольческой армии, беженцев из Центральной России, коренных 
  крестьян. При захвате станиц мобилизации подвергались все казаки призывного 
  возраста. В горной полосе действовали отряды горцев, часто терроризировавших 
  славянских поселенцев. Отношения между «бело-зелеными» и горцами были противоречивы, 
  но их сплачивала ненависть к Советской власти. <br>
  <br>
  По политической направленности можно выделить течения повстанцев. Во-первых, 
  казачье демократическое движение «самостийников». И большевики, и эсеры сходились 
  в оценке этого течения как жизненного и глубоко укоренившегося в массовых настроениях. 
  Основная часть рядовых повстанцев, в отличие от вожаков, отмежевывалась от России 
  поневоле, желая избавить хотя бы свой край от «коммуны»<sup>12</sup>. Во-вторых, 
  сохранялось монархическое движение офицеров ы части зажиточных казаков, не имевшее 
  устойчивой массовой поддержки. Оно подпитывалось извне белой эмиграцией. В-третьих, 
  крестьянские выступления за «советы без коммунистов» в эсеровском духе. На Ставрополье 
  и Черноморье их возглавляли опытные борцы участники волнений 1905 г.<sup>13</sup> 
  Большевики всерьез опасались ухода к повстанцам популярных «красных командиров», 
  что объясняет трагическую участь Б.М. Думенко, Ф.К. Миронова и стойкую подозрительность 
  властей к красным партизанам<sup>14</sup>. В-четвертых, анархистское движение 
  крестьян, малоотличимое от уголовщины. На Юге России оно не получило развития, 
  кроме рейдов махновцев по окраинам Дона. <br>
  <br>
  Листовки и оперативные документы повстанцев, выявленные нами, дают возможность 
  сделать вывод: основные лозунги движения — не узкосословные консервативные, 
  а в духе «третьей революции» на пользу всем сословиям. В документах Кубанской 
  повстанческой армии (КПА), отряда К.Т. Вакулина и других можно выделить общее 
  ядро призывов. Это — неприкосновенность «трудовой» собственности и раздел помещичьих 
  земель; равенство сословий (кроме новоселов края за 1914-1920 гг.); власть свободно 
  избранных советов без коммунистов и ЧК; окончание братоубийственной войны; созыв 
  Учредительного собрания; прощение сдавшихся красноармейцев и советских служащих; 
  антисемитизм<sup>15</sup>. Устойчивая былая вражда казаков с коренными крестьянами 
  и горцами затихла перед лицом общей угрозы. Ненависть обратилась на коммунистов 
  и переселенцев<sup>16</sup>. В листовках выражены противоречия между течениями 
  «бело-зеленых». Терцы и линейные казаки не выдвигали призывов к автономии. Напротив, 
  черноморцы и донцы намеревались провозгласить казачьи республики. Крестьянский 
  вариант «самостийности» представлен в воззваниях ополченцев Черноморья, составленных 
  эсерами Вороновичем и Рощенко<sup>17</sup>. Примечательны листовки штаба Русской 
  армии (монархической организации в Ейском отделе) с призывом объединиться всем 
  течениям: «Лозунги выбирать, начиная от монархических и кончая Учредительным 
  собранием, в зависимости от того, какие нравятся в данной местности»<sup>18</sup>. 
  <br>
  <br>
  Военные действия имели отчетливый сезонный ритм, затухая на время посевной и 
  жатвы, разгораясь осенью и ранней весной. Пики выступлений приходятся на февраль-март 
  и сентябрь 1921 г., что совпадает с особым обострением продовольственного кризиса 
  и переломными ситуациями в аграрной политике РКП(б). <br>
  <br>
  Падение белого Крыма создало в ноябре 1920 г. качественно новое соотношение 
  сил. Теперь надежды оппозиции на победу в борьбе регулярных войск рухнули. Большевикам 
  противостояли уже не армии, а разрозненные отряды повстанцев различных ориентаций. 
  Но Гражданская война с разгромом Врангеля отнюдь не завершилась. Она перешла 
  в иную кровопролитную стадию на внутренних фронтах. Как справедливо отмечают 
  В.П. Данилов и Т. Шанин, трагизм крестьянской войны — в противоборстве сил, 
  близких по социальному составу и организации, сторонников обновления страны 
  в интересах трудящихся<sup>19</sup>. <br>
  <br>
  Почему война затянулась в основных зерновых и окраинных регионах России до 1922 
  г., а в ряде местностей (Кубань, Терек) — до осени 1924 г.? Основная причина 
  — непомерное ужесточение политики «военного коммунизма» после установления контроля 
  над всей страной, усугубленное незнанием специфики зажиточных регионов и нежеланием 
  учитывать ее. Из этого следовали частные задачи. Член Кавбюро ЦК РКП(б) А.Г. 
  Белобородов утверждал: «единственно правильной мерой для Северного Кавказа... 
  является оккупация этого района советскими войсками»<sup>20</sup>. Плановые 
  задания разверстки и ее изъятие истощали сельское хозяйство края, вызывали сплочение 
  хлеборобов против власти. Доклад члена коллегии ВЧК М.И. Лацисом: и отчет особоуполномоченного 
  ВЧК К.И. Ландера (декабрь 1920 г.) свидетельствуют о создании военного режима 
  на Кубани и Дону. Области были поделены на сектора, секции и подсекции. В каждой 
  действовали «специальные тройки» представителей ВЧК по разоружению казаков и 
  выявлению «врагов Советской власти». Тройки имели право вершить казнь без суда. 
  Впоследствии за система была применена в Тамбовской губернии<sup>21</sup>. <br>
  <br>
  Недовольство земледельцев нарастало как лавина. Россия подошла зимой 1920-21 
  гг. к грани полномасштабной войны между государством и крестьянством, разочарованном 
  в системе правления. Оперативное управление штаба войск внутренней службы РСФСР 
  к началу 1921 г. содержало на учете 71 248 повстанцев, из них 19 640 — на Северном 
  Кавказе<sup>22</sup>. Включая кронштадских мятежников и мелкие отряды, в марте 
  насчитывалось до 118 000 «политбандитов»<sup>23</sup>. На Х съезде РКП(б) В.И. 
  Ленин оценил обстановку: «Мы оказываемся втянутыми в новую форму войны, новый 
  вид ее, которые можно объединить словом: бандитизм». Eмy вторил Н.И. Бухарин 
  — действия идут «иногда в форме настоящей войны, иногда в форме, чрезвычайно 
  близкой к этой войне»<sup>24</sup>. <br>
  <br>
  Первые признаки ухудшения ситуации на Северном Кавказе отмечались в октябре-ноябре 
  1920 г. в Донецком и Верхнедонском округах из-за невыполнимой разверстки и репрессий. 
  Возникли повстанческие отряды полковника Смелова-Аржеусова, Колычева, Колесникова 
  числом свыше 5000 человек. Смелов и Гученко возглавили в декабре крупное восстание 
  крестьян Ефремово-Степановской волости Донецкого округа, избрали на сходе «Временное 
  советское правительство», но быстро были разбиты<sup>25</sup>. 17 декабря президиум 
  Донисполкома признал Верхнедонской и 1-й Донской округа «неблагополучными по 
  бандитизму и контрреволюционным восстаниям» и просил РВС Кавказского фронта 
  подавить выступления. Проведены массовые аресты и взяты заложники<sup>26</sup>. 
  <br>
  <br>
  Показатель остроты кризиса - массовое дезертирство из РККА. Красная армия была 
  преимущественно крестьянской по составу. Тревожные вести от родственников о 
  разорительной разверстке, необеспеченность существования сделали многие воинские 
  части ненадежными. Как подсчитала исследовательская группа КГБ и МВД СССР, за 
  1921-1922 гг. зарегистрировано 58 397 дезертиров. 32 773 (56,1%) из них бежали 
  из РККА именно при «подавлении контрреволюции и антисоветских выступлений»<sup>27</sup>. 
  Обычно дезертиры прибегали к пассивному протесту, т.е. прятались в труднодоступных 
  местностях. Но зимой 1920-1921 гг. они все чаще переходят к повстанчеству. Так, 
  17 декабря 1920 г. бунт жителей слободы Михайловской Усть-Медведицкого округа 
  поддержал караульный батальон окружного военкомата под командованием К.Т. Вакулина 
  — большевика, орденоносца, бывшего комполка. Вакулин издал воззвание от имени 
  «трудовиков и восставших». Он призывал защитить настоящие свободу, равенство 
  и братство от «власти кучки коммунистов и комиссародержавцев». Повстанцы требовали 
  свободной торговли и хождения денег белых правительств. Весьма характерна социалистическая 
  риторика вакулинцев: организовать крестьянские союзы и «диктатуру деревни»; 
  разрешить все партии, кроме черносотенных и монархических: «Да здравствует Советская 
  власть без коммунистов и комиссаров!»<sup>28</sup>. Повстанцы накопили до 6000 
  штыков и 440 сабель, прорвались в Поволжье. Вакулинцы захватили Камышин, но 
  в марте 1921 г. были разгромлены.<sup>29</sup><br>
  <br>
  На сторону махновцев перешел в середине января 19-й полк 4-й дивизии 1-й Конной 
  армии под командованием Г.С. Маслакова — сподвижника С.М. Буденного, орденоносца. 
  Маслаков вошел командующим группой в штаб махновцев, объявил о создании «Кавказской 
  повстанческой армии». Мятеж готовился издавна, в полку велась агитация. Маслаковцы 
  выступили за «истинную Советскую власть без коммунистов и продразверстки». Совершив 
  рейд с Украины на Дон, повстанцы раздавали хлеб с ссыпных пунктов, громили ревкомы. 
  К ним присоединилось свыше 200 красноармейцев Дона. В 1-м Донском округе Маслаков 
  включил в свой состав отряд Сизова, довел численность «армии» до 5000 человек. 
  В марте был разбит под станцией Ремонтное Сальского округа, совершил рейд через 
  Сальские степи на Ставрополье. К сентябрю 1921 г. повстанцев загнали в горы, 
  где Маслаков и его соратник Брова были убиты амнистированными участниками отряда<sup>30</sup>. 
  <br>
  <br>
  На Кубани мятеж подняли некоторые подразделения 4-й, 7-й и 16-й дивизий. Больше 
  всего известно о «Кубанской повстанческой армии» И.Г. Захарченко, действовавшей 
  в районе станиц Воронежской и Кавказской. Судя по воззваниям, дезертиры были 
  недовольны «жестокостями комиссаров и коммунистов», разверсткой, развалом «Святой 
  Руси». Выдвигались задачи обеспечить свободный труд, созвать Учредительное собрание. 
  В апреле отряд (200 человек) разгромлен, предводитель скрылся<sup>31</sup>. 
  <br>
  <br>
  Сравнение повстанческих документов позволяет сделать вывод, что в 1921 г. преобладали 
  демократические призывы в эсеровском духе, приспособленные к запросам казачества. 
  Типичны требования: власть советов без коммунистов и ЧК; отмена продразверстки 
  и введение свободной торговли; война «всем саботажникам Советской власти и закомиссарившимся», 
  автономия или независимость Юга России. Маслаков объявлял себя «защитником интересов 
  вольного донского казачества»<sup>32</sup>. <br>
  <br>
  Глубокий кризис системы «военного коммунизма» угрожал самому существованию власти 
  РКП(б) на Северном Кавказе. 1 февраля 1921 г. представитель Донской ЧК докладывал 
  пленуму облисполкома, что «положение в области тяжелое. Масса антисоветских 
  проявлений в широких слоях населения». В информсводке Донской ЧК за первую половину 
  февраля отмечена угроза восстаний с участием рядовых коммунистов, неэффективность 
  управления войсковыми частями. 10 марта председатель ДонЧК М. Буров писал: «Донская 
  область является одной из баз контрреволюции... Нужно вычистить железной рукой. 
  Армия разложена и в случае восстания Советская власть на Дону — да пожалуй, 
  и во всем крае, — не будет иметь достаточной надежной вооруженной силы, что 
  и подтверждают факты перехода к бандам целых войсковых частей»<sup>33</sup>. 
  По признанию Кубано-Черноморского обкома РКП(б), к весне было трудно отыскать 
  станицы вне влияния «бело-зеленых». Делегат II областной партконференции Асаульченко 
  утверждал, что большевики «еле держались» в станицах. «Те массы, которые били 
  Врангеля, теперь бьют коммунистов»<sup>34</sup>. <br>
  <br>
  Трагизму положения соответствовала решительность военно-репрессивных мер власти. 
  12 января 1921 г. ЦК РКП(б) создал две комиссии с целью «спешно подготовить 
  меры военной ликвидации бандитизма». Сходные директивы принимались на краевом 
  и областном уровнях. Так, участники закрытого заседания руководства Кубано-Черноморья 
  просили Кавбюро ЦК 13 марта усилить военное присутствие, создать отряды по борьбе 
  с «бандами» из бывших красных партизан, заменить сочувствующих казакам милиционеров 
  выходцами из Центра России. Полномочия Советов передавались их президиумам, 
  что означало возврат к ревкомам<sup>35</sup>. <br>
  <br>
  Первые вести о продналоге достигли станиц Северного Кавказа к концу марта. Волна 
  восстаний спала, была отсрочена угроза сплочения всех недовольных. Однако продналог 
  одобряли гораздо сдержаннее, чем в Центральной России. Ведь казаки долгие века 
  не платили прямых налогов. Доверие к новшеству уменьшалось из-за проднаряда, 
  понятого как «новая хитрость коммунистов» и усугубившего сомнения в прочности 
  власти. Продолжавшиеся поборы и потравы посевов красноармейцами, грубость продотрядов 
  озлобляли хлеборобов, толкали их к продолжению борьбы<sup>36</sup>. Ввод продналога 
  стал не окончательным, а промежуточным рубежом повстанчества. <br>
  <br>
  Политическую обстановку 1921 г. можно оценить только при целостном анализе факторов: 
  общественного мнения; социальной базы и лозунгов «бело-зеленых»; степени контроля 
  над территорией; стратегии и тактики противников. Начало исследований политических 
  настроений земледельцев положил Н.Г. Цыганаш, установив резкое повсеместное 
  улучшение оценок власти в апреле 1921 г., значительный рост недовольства в мае—июне 
  и декабре 1921 г.<sup>37</sup> «Обзор парторганизаций РСФСР к 5 октября 1921 
  г.» содержит оценку: «Отношение большинства казаков к Коммунистической партии 
  резко отрицательное, причем они определенно разделяют (так в тексте. — А.Б.) 
  Советскую власть от коммунистов... К новой экономической политике казачество 
  относится недоверчиво»<sup>38</sup>. Напротив, «иногородние» воспринимали РКП(б) 
  как защитницу и часто подменяли классовые интересы узкосословными. <br>
  <br>
  Идейная ориентация повстанцев отличалась преобладанием лозунгов эсеровского 
  «трудовластия» и казачьей «самостийности». Типичны листовки Кубанской повстанческой 
  армии, написанные Пилюком и Савицким. В них «кубанский народ» (все жители края 
  до 1914 г.) противопоставлен «паразитам-пришельцам». Требования — независимая 
  Кубань, истинное народоправство; свобода личности «без подвалов и чрезвычаек», 
  земля для всех трудящихся, взятая без выкупа у помещиков и капиталистов; неприкосновенность 
  трудовой собственности. Авторы пытались уверить, что «власть новых русских дворян—господ 
  коммунистов, получивших ханский ярлык на Кубани» не имеет ничего общего с нуждами 
  тружеников-красноармейцев. Призыв установить «истинную советскую рабоче-крестьянскую 
  власть» сочетался с грубо-сепаратистским лозунгом: «не надо нам ни монархистов, 
  ни коммунистов, Кубань для кубанцев»<sup>39</sup>. Из обзоров ВЧК известно о 
  противоположной, монархической тенденции, но источников ее оценки мало. Отметим 
  «Штаб помощи Русской армии полковника М.Н. Жукова. Он, как и «самостийники», 
  пытался привлечь к себе иногородних, так как «они тоже терпят гнет Коммуны». 
  Но его листовки отличали идеи защиты православия и непримиримость к нерусским 
  народам<sup>40</sup>. <br>
  <br>
  Документы РККА подтверждают широкий размах Гражданской войны. До конца 1922 
  г. военное положение сохранялось в 36 губерниях, областях и автономиях РСФСР<sup>41</sup>. 
  По данным Статистического управления РККА, боевые потери регулярных частей за 
  1921 г. превысили 170 000 человек, а за 1922 г. — почти 21 000 по стране<sup>42</sup>. 
  Из этих 170 000 РККА безвозвратно потеряла в боях против «контрреволюционных 
  и антисоветских выступлений» 58 397 человек, в том числе Кавказский фронт — 
  9338 человек.<sup>43</sup> По мнению большевиков, весной 1921 г. на Кубани и 
  Тереке «формировались целые повстанческие армии из сохранившихся бандитских 
  кадров»<sup>44</sup>. 5 июня штаб СКВО признавал в докладной Реввоенсовету Республики: 
  весь округ — в чрезвычайных условиях. «Южная и юго-западная часть Кубано-Черноморской 
  области представляют собой почти непрерывный фронт борьбы шаек, причем Ейский, 
  Краснодарский, Майкопский и Баталпашинский отделы наиболее опасны по характеру 
  и степени развития бандитизма и требуют максимального напряжения...»<sup>45</sup>. 
  Весной, по отчету Терского губкома РКП(б), «контрреволюцией кишмя кишат станицы...»<sup>46</sup>. 
  <br>
  <br>
  Сложилось несколько устойчивых очагов вооруженного бунтарства на Юге: Закубанье, 
  Пятигорье, Верхний Дон, Приазовье. Их расположение зависело от комплекса геополитических 
  факторов: рельефа местности (леса и плавни Кубани и Терека предпочтительнее 
  донских степей), возможностей контактов с эмиграцией, степени истощения хозяйства 
  и наличия беспокойного населения. Как и в 1920 г., ход военных действий в каждом 
  очаге был автономен. Всеобщая почва протеста — сохранность «военно-коммунистических» 
  методов управления. <br>
  <br>
  В горах Закубанья в начале февраля образовалось Кубанское повстанческое правительство 
  эсеровской ориентации. Началось формирование его армии во главе с генералом 
  от инфантерии М.А. Пржевальским. Баталпашинский отдел стал местом сосредоточения 
  Народных войск Северного Кавказа полковника Даутокова (псевдоним — Серебряков). 
  В Пятигорске действовал подпольный «Реввоенсовет Зеленой армии», готовивший 
  восстание 11 станиц. Приазовские плавни служили базой отрядов В.Ф. Рябоконя 
  и М.Н. Жукова<sup>47</sup>. Наметившийся после объявления продналога спад движения 
  сменился новыми вспышками, как только было объявлено о единовременном проднаряде. 
  В Кубано-Черноморье численность повстанцев к 1 мая составила 1756 штыков и 2774 
  сабли в 50 отрядах, к 1 июня — 1356 штыков и 1902 сабли в 36 отрядах. Эти расчеты 
  разведотдела штаба 9-й армии РККА включали в себя только достоверно известные 
  отряды силой более 15 человек.<sup>48</sup> На Тереке «зеленые» умножились с 
  700 сабель накануне объявления продналога до 3520 сабель и 365 штыков к середине 
  июня<sup>49</sup>. Даже на обескровленном Дону после весеннего сева отряды в 
  200-300 человек за несколько недель увеличились в 3-5 раз, начали активизироваться<sup>50</sup>. 
  <br>
  <br>
  Весной 1921 г. наибольшую активность на Кубани проявляли отряды Жукова в Приазовье 
  и 1-я Кубанская повстанческая армия Серебрякова в Баталпашинском отделе. Жуков 
  во главе 500 человек совершал частые налеты на Ачуевские рыбопромыслы и станицу 
  Уманскую, рассылал агентов для подготовки восстаний. 21 апреля была захвачена 
  станица Чепигинская, где «бело-зеленые» расправились с членами коммуны «Набат», 
  выселившими монахов из Екатериноебяженской пустыни<sup>51</sup>. Близ станицы 
  Кущевской убит эмиссар областной ЧК М.В. Полуян, требовавший под угрозой расстрелов 
  усилить хлебозаготовки<sup>52</sup>. В ответ ЧК провели массовые аресты и расстрелы 
  в Ейском отделе. Вскоре Жуков был разгромлен, чудом скрылся и возглавил подпольную 
  Организацию белого креста<sup>53</sup>.<br>
  <br>
  Полковник Серебряков к маю объединил отряды Баталпашинского и Лабинского отделов 
  (до 3000 человек) и создал штаб армии во главе с полковником Трубачевым. К нему 
  присоединились Зеленчукский и Кардоникский полки Д. Арканникова и князя О. Джентемирова 
  из линейных казаков. Общими силами они двинулись на соединение с отрядами Майкопского 
  отдела через станицы Баталпашинскую и Невинномысскую. Предпринимались частые 
  налеты на Армавир и Кисловодск, железную дорогу Ростов-Баку<sup>54</sup>. К 
  середине мая 16-я кавдивизия РККА (начдив Я.Ф. Балахонов) и отряд в составе 
  34-й стрелковой дивизии нанесли поражение основным силам Серебрякова вблизи 
  Невинномысской и вытеснили плохо вооруженных мятежников в горы. 17 мая Серебряков 
  и его помощники были схвачены у хутора Надзорного и казнены. К концу мая разрозненные 
  остатки «армии» укрылись в горах<sup>55</sup>. <br>
  <br>
  Терцы образовали весной по всей области гибкий подвижный фронт, стремясь отвлечь 
  РККА от ударов по кубанским повстанцам. Силы «зеленых» за март — середину мая 
  выросли с 700 до 1800 человек. Крупнейшим отрядом (700 человек) располагал Маслаков, 
  ушедший к Кизляру<sup>56</sup>. На Ставрополье принудительный сбор проднаряда 
  вызвал восстания в Медвеженском, Александровском и Ставропольском уездах<sup>57</sup>. 
  Представляет интерес сообщение терского эсера в журнале «Революционная Россия» 
  о политических настроениях. По его мнению, неприятие репрессий значительно сгладило 
  «антагонизм казаков и иногородних», считавших своими угнетателями большевиков 
  и деникинцев. Казаки-де говорили: «Пусть теперь начинают крестьяне восстание, 
  а мы поддержим». Они наивно надеялись создать свое государство в пределах Северного 
  Кавказа, стихийно сочувствуя эсерам<sup>58</sup>. <br>
  <br>
  На Дону основным очагом восстаний оставался Верхнедонской вкруг, где в станице 
  Вешенской 14 марта из-за жестокого изъятия продуктов восстал караульный батальон 
  Я.Е. Фомина. В мятеже участвовали местные коммунисты. По сообщениям окружного 
  комитета РКП(б), власть сохранялась только в станицах, а хутора перешли под 
  контроль «бандитов»; проезд возможен лишь в сопровождении вооруженной силы<sup>59</sup>. 
  Численность повстанцев Верхнего Дона достигла 4600 человек в отрядах Колесникова, 
  Попова, Рыжкова и других. Разбить или рассеять их удалось к лету<sup>60</sup>. 
  <br>
  <br>
  Обстановка не стабилизировалась. По свидетельству С.М. Буданного, В.И. Ленин 
  говорил ему весной 1921 г., что на Северном Кавказе достаточно спички, чтобы 
  вспыхнул крупный пожар на манер кронштадского<sup>61</sup>. Показательны возражения 
  Л.Д. Троцкого и руководителей края (А.Г. Белобородова, Е.А. Трифонова) против 
  переброски 1-й Конной армии на Северный Кавказ. Они опасались перехода массы 
  голодных и озлобленных красноармейцев на сторону повстанцев. Вопрос решен в 
  пользу переброски на Маныч по настоянию С.М. Буденного, но на местах часто жаловались 
  на антикоммунистические действия конармейцев<sup>62</sup>. В мае 1921 г. командование 
  фронта докладывало С.С. Каменеву: «Бандитизм на Кавказе окончательно не ликвидирован, 
  и казачество не скоро согласится с требованиями, предъявленными властью пролетариата»<sup>63</sup>. 
  Причины состояния раскрыты в сводках ЧК. Земледельцы не уверены в прочности 
  власти, сдают дополнительную разверстку на скот и продналог под принуждением. 
  С момента введения проднаряда «отношение всех слоев и групп населения области 
  к Соввласти заметно ухудшилось»<sup>64</sup>. <br>
  <br>
  Способы умиротворения края оставались преимущественно силовыми. Мозговым центром 
  региональной системы управления стали чрезвычайные «тройки» органов РКП(б), 
  созданные на краевом, областном и местном уровнях<sup>65</sup>. 30 мая 1921 
  г. Юго-Восточное бюро РКП(б) направило обкомам и губкомам циркулярное письмо, 
  в котором предложило усилить агитацию против «бандитизма». Для «явно контрреволюционных 
  сел» предусматривалась система заложников. «В отношении станиц и деревень, упорно, 
  активно содействующих бандам и неисправимо контрреволюционным, необходимо после 
  исчерпания всех указанных мер применять меры карательные вплоть до полного уничтожения 
  станицы или села и массовых расстрелов». Подводился итог: «Ликвидация бандитизма 
  и создание прочных основ существования рабоче-крестьянской власти в Донской, 
  Кубано-Черноморской областях, Терской и Ставропольской губерниях является в 
  данный момент основной боевой задачей» всех органов власти<sup>66</sup>. <br>
  <br>
  Для подавления мятежей укреплено руководство Северо-Кавказскими военным округом 
  (с июля 1921 г. командующий К.Е. Ворошилов, члены РВС — С.М. Буденный и А.С. 
  Бубнов). 9 июля РВС СКВО создал краевое, областные и окружные военные совещания 
  по борьбе с бандитизмом. В состав совещаний входили: командир воинского соединения 
  (председатель), секретарь парторганизации и представитель ВЧК. Совещания получили 
  верховную власть в крае. Центральная межведомственная комиссия по борьбе с бандитизмом 
  даже посоветовала в августе создать комитеты бедноты, что показывает накал страстей. 
  К счастью, эта крайняя мера не была реализована<sup>67</sup>. <br>
  <br>
  Наряду с привычным насилием объявлялись амнистии для главарей «банд», которые 
  вернутся к мирной жизни и приведут с повинной всех бойцов с оружием и снаряжением. 
  Амнистия охватывала также дезертиров из РККА и беглецов из концлагерей. Приказ 
  №1 крайвоенсовещания сулил им «особые охранные листы», а в случае полного раскаяния 
  и благонадежности — восстановление гражданских прав. Все сдавшиеся подлежали 
  учету в ВЧК и военкоматах. Срок добровольной явки истекал 1 сентября, после 
  чего должно было последовать беспощадное уничтожение. По словам Л.Д. Троцкого, 
  «красный каток истребит контрреволюционные гнезда»<sup>68</sup>. <br>
  <br>
  Невыполнение чрезмерного продналога и сохранение очагов повстанчества диктовали 
  ужесточить курс РКП(б) в крае. Донской обком запретил крестьянам, сдавшим меньше 
  1/2 налога, торговать зерном и одобрил 10 голосами против 9 конфискацию их товаров 
  на рынках. 15 сентября Кубано-Черноморский обком признал неотложной «оккупацию 
  воинскими частями станиц», не выполнивших продналог. Мужчины старше 18 лет из 
  семей повстанцев арестовывались, семьи главарей «банд» высылались на Соловки. 
  Местным исполкомам было дано право выносить смертный приговор. На Ставрополье 
  запрещен товарообмен между уездами<sup>69</sup>. <br>
  <br>
  В ответ произошла консолидация повстанцев. В июне на Нижнем Дону близ станицы 
  Елизаветинской объединилось до 1500 казаков. Они создали «Армию спасения России» 
  под руководством князя, генерал-майора К.Э. Ухтомского и полковника Ф.Д. Назарова. 
  Преобладала эсеровская идеология в силу состава «армии» — «бывших» маслаковцев 
  и сизовцев. Готовился удар по Ростову с одновременным мятежом в городе с призывом 
  «Долой коммунистов, долой натуральный налог». День мятежа был приурочен к началу 
  сбора продналога<sup>70</sup>. На Верхнем Дону продолжали действовать отряды 
  Фомина и Маслакова (до 240 человек). В Сальском и 1-м Донском округах образовался 
  штаб «Донской повстанческой армии» во главе с бывшим начальником штаба 14-й 
  кавдивизии 1-й Конной армии Абрамовым. Он выдвигал эсеровский призыв «Власть 
  советам, а не партиям»<sup>71</sup>. Во второй половине июля Ухтомский, Назаров 
  и их соратники были арестованы и осуждены. В районе ст. Елизаветинской Ростовского 
  округа состоялся съезд представителей повстанческих отрядов с участием С.М. 
  Буденного и крайуполномоченного ВЧК. Съезд принял решение сложить оружие, с 
  чем согласились почти все «зеленые» нижнего Дона<sup>72</sup>. <br>
  <br>
  Используя демобилизацию РККА и неприятие продналога, оживились летом 1921 г. 
  повстанцы Кубани. К середине июля «бело-зеленые» признали главенство Кубанской 
  повстанческой армии, призывы и планы которой разрабатывал бывший товарищ председателя 
  краевой Рады полковник П.А. Савицкий — осторожный, хитрый политик с диктаторскими 
  задатками. 18-19 июля он провел съезд вожаков отрядов близ станицы Бакинской, 
  узаконив избрание генерала Пржевальского командующим армией. 5 отрядов в 650 
  человек были переформированы. 23 августа съезд повстанцев близ станицы Гиагинской 
  решил предпринять рейд на Краснодар, используя временное истощение и рассредоточенность 
  9-й армии<sup>73</sup>. Проводился призыв казаков. В Закубанье к осени действовали 
  105 отрядов (2400 штыков и 4800 сабель при 111 пулеметах), в том числе отряд 
  полковника Белова (более 1000 человек). Костяк «армии» составили 6 полков в 
  730 человек под Горячим Ключом. Помощь им оказывали также отряды В. Рябоконя 
  (300 человек) и Е. Дубины (250 человек) в Приазовье<sup>74</sup>. <br>
  <br>
  В работе третьего съезда повстанцев 14-15 сентября участвовали представители 
  Терека и Ставрополья, эсер-эмигрант Н.В. Воронович, посланцы Русской армии из-за 
  рубежа<sup>75</sup>. После неожиданного захвата Краснодара, Савицкий собирался 
  вооружить призывников за счет городского арсенала, провозгласить Кубанскую республику 
  и повести переговоры с РСФСР о признании. Идеологи КПА Пилюк и Савицкий ошибочно 
  полагали, что объявленная в августе амнистия — признак слабости власти, а хлеборобы 
  повсюду поднимут мятеж. Внушали надежды беспечность штаба 22-й стрелковой дивизии 
  и разногласия между членами облвоенсовещания, не дававшие беспощадно подавить 
  мятеж. На наш взгляд, рейд КПА был продиктован отчаянием, а не трезвым расчетом. 
  Казачество уже жаждало мира, будучи обескровлено и запугано. Амнистии постепенно 
  ослабляли повстанцев. РККА могла наращивать присутствие в крае, а у «бело-зеленых» 
  истощились резервы. Поэтому бросок на Краснодар мог иметь лишь тактический временный 
  успех. <br>
  <br>
  В ночь на 18 сентября 1921 г. Кубанская повстанческая армия переправилась через 
  р. Кубань у аула Эдепсухай и заняла наступательный рубеж вдоль р. Кочеты, проводя 
  успешный набор казаков. Это стало неожиданным для частей 22-й дивизии. Пржевальский 
  имел 4 хорошо вооруженных кавалерийских полка, стрелковый батальон и штабную 
  сотню (всего — до 1000 человек), надеясь с захватом склада боеприпасов в городе 
  полностью укомплектовать части. В ночь на 20 сентября КПА заняла станицы Динскую 
  и Пашковскую, подошла к пос. Лорис и окружила Краснодар. <br>
  <br>
  Штурм города сорвался благодаря спешному прорыву С.М. Буденного, возглавившего 
  облвоенсовещание. Части 1-й Конной армии охватили осаждавших внешним кольцом. 
  Уже вечером 21 сентября КПА отошла за р. Кубань, а на рассвете 23 сентября была 
  разгромлена близ с. Белого. Пржевальский со штабной сотней скрылся в горах Туапсинского 
  отдела. КПА потеряла около 300 человек убитыми и 110 пленными<sup>76</sup>. 
  Попытка повстанческого правительства восстановить в октябре движение провалилась. 
  Пилюк и Савицкий были схвачены и осуждены<sup>77</sup>. <br>
  <br>
  Восточнее, в Баталпашинском отделе кавбригада В.И. Книги разбила отряды полковника 
  Белова и Васильева близ ст. Предгорной. Белов погиб, Васильева схватили<sup>78</sup>. 
  Доказала свою эффективность новая методика разгрома «банд» — засылка секретных 
  сотрудников ВЧК и создание «лжеповстанческих» отрядов. Так, в августе 1921 г. 
  ушел к зеленоармейцам командир дивизиона 16-й кавдивизии 1-й Конной армии В.И. 
  Логвинов. Он считался в движении будущим атаманом Терека. 5 ноября, организовав 
  съезд главарей отрядов, Логвинов уничтожил почти всех собравшихся и продолжал 
  «бить врага изнутри» (по его словам). Весной 1923 г. эскадрон Логвинова с почетом 
  был встречен в Пятигорске. Позже Логвинов доказал верность власти службой в 
  РККА<sup>79</sup>.<br>
  <br>
  Своеобразная обстановка сложилась летом-осенью 1921 г. на Тереке. Согласно августовскому 
  докладу начальника губернской милиции шло сплочение мелкие отрядов «бело-зеленых» 
  в более крупные, «производящие с большей дерзостью и жестокостью нападения на 
  отдельных граждан, хутора, села» и даже поезда. Особо неблагонадежны Моздокский 
  и Святокрестовский уезды, станицы Лысогорская, часто занимаемых местными «бандами»<sup>80</sup>. 
  В октябре 1921 г. на Тереке действовали отряды в 1300 сабель с 15 пулеметами, 
  в том числе крупнейшие: Хмары (350 человек) и Супрунова (250 человек) под Кисловодском, 
  Лаврова (200 человек) и Овчинникова (250 человек) от Моздока до Кизляра. Вблизи 
  Ставрополя сосредоточился отряд Беззубова (140 человек). Совершались частые 
  налеты на предгорные станицы. Характерно, что к казачьему ядру повстанцев присоединялись 
  кабардинцы, осетины, ставропольские крестьяне<sup>81</sup>. <br>
  <br>
  Власти приняли жесткие меры. На Терек был переброшен сводный отряд Апанасенко 
  в составе 1-й Конной армии. Налажено взаимодействие местных органов с соседней 
  Калмыцкой автономией. В селах и станицах созданы отряды самообороны. Эти факторы 
  вкупе с усиливавшимся голодом возымели действие. Отряды распадались и все чаще 
  переходили к уголовным действиям. Развернулась добровольная явка повстанцев 
  а плен. К началу 1922 г. в Терской области осталось 520 «бело-зеленых» при 6 
  пулеметах, в Ставрополье — вдвое меньше<sup>82</sup>. <br>
  <br>
  В меньшей мере вспышки активности мятежников проявлялись на Дону. В сентябре 
  1921 г. были арестованы руководители подпольного штаба «Донской повстанческой 
  армии» Абрамов и Шонполонский. На Нижнем Дону осталось к 1922 г. лишь 215 штыков<sup>83</sup>. 
  В Верхнедонском и Донецком округах, напротив, движение расширялось из-за принудительного 
  сбора продналога. Численность отряда Фомина и Кондратьева достигла 220 человек. 
  Казаки пытались сорвать налоговую кампанию, уничтожая продработников и документацию 
  в исполкомах, проводя местные мобилизации в отряды. 6 октября 1921 г. Фомин 
  от имени «восставших войск военно-революционного времени» призвал жителей к 
  мятежу: «коммуна приведет к голодной смерти, к рабству крепостного права». 13-16 
  октября его отряд занимал станицу Вешенскую, но поддержки казаков не получил<sup>84</sup>. 
  30 октября Кондратьев был разбит и схвачен. Отряд Фомина разделился, ушел на 
  переформирование в Хоперский округ и до конца года дважды совершал налеты на 
  станицы Вешенскую, Каргинскую и Еланскую. У него осталось 90 человек, Фомин 
  единственным на Дону сохранял политические (эсеровские) призывы борьбы<sup>85</sup>. 
  Остальные вырождались в уголовников. <br>
  <br>
  В ходе осеннего разгрома «бело-зеленых» органы РККА и ВЧК усовершенствовали 
  систему репрессий. В местности, «особо зараженные бандитизмом», посылались выездные 
  сессии Революционного трибунала СКВО и политические «тройки», учреждались полевые 
  комендатуры. Проводилась поголовная проверка мужчин — жителей станиц, брались 
  заложники из числа родственников и пособников «бело-зеленых»<sup>86</sup>. Так, 
  22 сентября Военсовещание Кубано-Черноморской области дало неделю на добровольную 
  сдачу оружия, после чего «политбандитов» и пособников ждал расстрел, а их семьи 
  подлежали высылке в Архангельскую губернию с конфискацией имущества. Только 
  политкомиссии 22-й дивизии расстреляли с 20 сентября до 1 ноября 1921 г. 3112 
  человек (по неполным данным)<sup>87</sup>. Протоколы политкомиссии 193-го стрелкового 
  полка подтверждают произвол. К смерти приговаривали «за сожительство с бандитом», 
  за классовую и сословную принадлежность, за бытовое недовольство властью. Встречались 
  формулировки типа: «вообще элемент бандитский 68 лет»<sup>88</sup>.<br>
  <br>
  Тактика вооруженной борьбы изменилась коренным образом с зимы 1921 г. Советские 
  войска значительно окрепли и приобрели опыт сражений в местных условиях. Повстанцы 
  уже не могли владеть инициативой и перешли к скоротечным налетам мелких отрядов. 
  Их численность сократилась с 1 октября по 31 декабря 1921 г. с 6180 до 1210 
  человек, оснащение — с 74 до 23 пулеметов, число отрядов — с 88 до 29 на территории 

  СКВО<sup>89</sup>. Все чаще уставшие от войны крестьяне и казаки отказывали 
  повстанцам в снабжении и создавали при поддержке РККА и ВЧК отряды самообороны. 
  Недовольство земледельцев теперь проявлялось по частным поводам и не получало 
  массово организованных форм<sup>90</sup>. <br>
  <br>
  Почему же очаги вооруженного сопротивления сохранялись на Северном Кавказе до 
  осени 1924 г.? Главная причина этого — противоречивость и непоследовательность 
  раннего нэпа. Принудительный сбор продналога и скованность сельского рынка были 
  в числе основных элементов хозяйствования. Фискальный нажим достиг предела к 
  началу 1922 г., а план продналога на Юге России был выполнен только на 43%. 
  Исчерпание запасов продовольствия вызвало массовый голод<sup>91</sup>. В недоимочные 
  районы были введены войска, которым партийные органы дали право «широкой эксплуатации 
  злостных неплательщиков». Коллегии трибуналов не успевали выносить приговоры 
  и приходилось создавать дополнительную сеть карательных органов<sup>92</sup>. 
  Подливали масла в огонь бесчинства некоторых красноармейцев, живших впроголодь 
  и вынужденных кормиться за счет поборов. Так, в мае 1922 г. части 4-й Петроградской 
  и 6-й Чонгарской дивизии, не подчиняясь гражданским властям, вершили произвол 
  в Армавирском отделе<sup>93</sup>. Однако члены Юго-Восточного бюро ЦК РКП(б) 
  продолжали считать причиной бандитизма «наличие в крае большого процента казачьего 
  населения, все еще не смирившегося с фактом существования Советской власти»<sup>94</sup>. 
  Иностранный отдел ВЧК сообщал 10 февраля 1922 г. о подготовке десантов врангелевской 
  армии под предлогом обеспечения помощи голодающим<sup>95</sup>. <br>
  <br>
  Весной 1922 г. усилились очаги повстанческого движения в плавнях Приазовья и 
  предгорьях Кавказа. Полковник М.Н. Жуков, сменив лозунг «Советы без коммунистов» 
  на монархический — «Единую и неделимую Россию», координировал действия на большей 
  части Кубани. Его главенство признали: «Союз Южно-Русского народа» в Приазовье 
  (группа полковника Строганова), бригада есаула Беззубова и «армия» полковника 
  Белова в Лабинском отделе, отряд сотника Е. Дубины в Ейском отделе. Численность 
  контактирующих группировок к весне достигла 665 человек при 12 пулеметах<sup>96</sup>. 
  Помощь КПА генерала Пржевальского, возрождаемой в Закубанье, сорвалась из-за 
  политических противоречий.<br>
  <br>
  Краевые и местные органы власти применили новую, более гибкую и успешную тактику, 
  направив в тревожные районы лучших работников и проводя беспартийные конференции 
  для агитации. ВЧК успешно засылала к повстанцам агентов, разлагая отряды изнутри<sup>97</sup>. 
  К середине апреля рост недовольства удалось остановить. В «угрожающих» Ейском 
  и Армавирском отделах возобновлены военные комендатуры. За вторую половину апреля 
  — первую половину мая 1922 г. в Ейском отделе предпринят «красный каток» против 
  отрядов Е. Дубины. Сильные подразделения 22-й дивизии и ЧОН сорвали мятеж в 
  станице Уманской и разгромили отряды Дубины. За май-июнь в отделе расстреляны 
  680 человек, в том числе 90 повстанцев, 443 «пособника» и 143 заложника<sup>98</sup>. 
  В боях погибли 82 бойца «Корниловского отряда», 119 ранены<sup>99</sup>. Этим 
  была сорвана попытка массового восстания при последующей высадке врангелевцев<sup>100</sup>. 
  <br>
  <br>
  В остальных местностях Северного Кавказа весной 1922 г. не наблюдалось серьезных 
  выступлений. На Верхнем Дону 20 марта разгромлен отряд Фомина, причем население 
  поддержало органы власти<sup>101</sup>. К осени Дон полностью умиротворен. На 
  Ставрополье сохранились отряды Беззубова и Мордачева, связанные с подпольными 
  организациями в городе и селах. Они занимались грабежами и убийствами сторонников 
  власти. Политические лозунги сохраняли только отряды Тишкова в Кизлярском уезде 
  и Шаталова на Сунже (243 человек)<sup>102</sup>. За июнь усилилась бригада есаулов 
  Арканникова и Беззубова (200 человек), угрожавшая окраинам Ставрополя. Группировка 
  Белова на юге Лабинского отдела объединила 7 отрядов в 150 сабель, выступая 
  под монархическими лозунгами. К августу их силы полностью разбиты<sup>103</sup>. 
  <br>
  <br>
  Итоги весенних военных действий однозначны. Численность повстанцев в пределах 
  СКВО сократилась за май и июнь 1922 г. с 2708 до 1567 человек при том же количестве 
  отрядов<sup>104</sup>. Закрытое письмо Юго-Восточного бюро ЦК поясняет причины 
  спада движения: «Если раньше значительные слои деревни сочувствовали и всемерно 
  поддерживали бандитизм, то ныне деревня, ищущая порядка, в большинстве своем 
  враждебна бандам, поскольку они грабят также и население. При массовых операциях 
  даже казаки активно боролись против банд. Только верхушки кулачества... продолжают 
  являться опорой бандитизма»<sup>105</sup>. А.И. Микоян отмечал повсеместную 
  небывалую тягу к мирному труду, что постепенно заставляло изолированных повстанцев 
  сдаваться<sup>106</sup>. Немногие оставшиеся отряды утрачивали политические 
  цели и скатывались к уголовщине<sup>107</sup>. <br>
  <br>
  Окрепшие структуры власти позволили себе более гибкую и совершенную тактику 
  борьбы. В июне 1922 г. Кубано-Черноморский обком РКП(б) дал указания на места 
  по-особому относиться к различным группам повстанцев: добровольно сдавшимся, 
  сражающимся на стороне РККА против былых соратников, продолжающим сопротивляться<sup>108</sup>. 
  Весомую роль в умиротворении края сыграли общероссийские законодательные акты 
  об упрощении налогообложения, о расторжении кабальных сделок под залог имущества. 
  Земельный кодекс, вступивший в силу осенью 1922 г., подтвердил сложившийся порядок 
  надельного пользования в казачьих областях<sup>109</sup>. <br>
  <br>
  Завершением повстанческих движений стали их всплески осенью 1922-1923 гг. В 
  понимании причин, кроме постоянного фактора сезонной активности, могут помочь 
  документы самих «бело-зеленых». Доступен текст донесения в эмиграцию подъесаула 
  И.Ф. Малогутия из окрестностей Майкопа, а также воспоминания эмигранта С. Макеева 
  — участника диверсионного десанта 1923 г. на Кубань. Малогутий оценивал власть 
  резко отрицательно: руководят-де «малограмотные мужики». Армия плохо воспитана 
  и оснащена, малонадежна. Финансовое положение страны близко к краху, но переворот 
  не предвидится, так как надежда казаков на успех восстаний и помощь зарубежья 
  подорвана<sup>110</sup>. В этом послании примечательно явное преувеличение шаткости 
  власти при объективной оценке психологического тупика, в котором оказались казаки. 
  С. Макеев рисует в мемуарах яркую картину безысходности, раскрывает обреченность 
  десантов из-за рубежа. «Народ лучше нас знает, что ему нужно... Народ жаждет 
  мира, спокойствия, земли и воли, и кто даст все это, за тем он и двинется»<sup>111</sup>. 
  <br>
  <br>
  Тем не менее, крупномасштабные сражения заняли весь период с осени 1922 г. по 
  весну 1923 г. В пределах Кубано-Черноморья можно проследить их динамику. Осенью 
  оставалось 900 повстанцев, кроме 120 сложивших оружие. Все чаще встречались 
  мелкие и долго бездействовавшие отряды (например, 7 человек во главе с В.Ф. 
  Рябоконем близ станицы Гривенской)<sup>112</sup>. Зимой они заметно ослабли 
  и начали добровольно сдаваться. К началу 1923 г. на учете ОГПУ в русских областях 
  Юга состояли только 12 отрядов в 33 штыка и 249 сабель. В Горской АССР и Дагестане 
  бандитизм устойчиво сохранялся все 1920-е гг., но имел национально-религиозные 
  причины и не составлял единого фронта с движением казаков и крестьян<sup>113</sup>. 
  <br>
  <br>
  Мы систематизировали статистические сведения из кратких периодических обзоров 
  СКВО и расписаний сил противника, составленных на основе разведданных. Они позволяют 
  проследить изменения численности и дислокации повстанцев (см. таблицу в приложении). 
  Отметим большое число добровольно сдавшихся и возобновивших борьбу через 2—3 
  месяца отрядов, постоянство основных очагов «зеленоармейства» в предгорьях Кавказа 
  и плавнях Приазовья<sup>114</sup>. Из данных таблицы явствует: наибольшая массовость 
  и организованность протеста фиксировалась на Кубани и Черноморье (44—64% участников 
  и от 19 до 58 бойцов в среднем на отряд); на Тереке (20—30% участников и от 
  12 до 120 бойцов). Наименьшая активность отмечалась в Ставрополье (5—10% участников 
  и от 18 до 42 бойцов на отряд). За осень 1921 — лето 1923 гг. неуклонно снижалась 
  концентрация повстанцев (от 70 до 15 бойцов на отряд). Эти расчеты, будучи дополнены 
  по другим периодам и детализированы по местностям, могут стать основой количественной 
  модели движения. <br>
  <br>
  Подтасовка выборов в Советы и хозяйственный кризис 1923 г. способствовали местным 
  вспышкам протеста. Так, 13 января отряд в 10 человек напал на станицу Сущевскую 
  с целью освободить заключенных, но был отбит. 22 марта «зеленоармейцы» во главе 
  с Адриановым, добровольно сдавшиеся в Сальском округе, призвали крестьян не 
  платить налоги и после боя бежали в степь. Хорошо оснащенные группировки действовали 
  вокруг станиц Суворовской, Бекешевской и Сторожевой. Исключением на фоне уголовников 
  выглядел отряд «Червонный валет» в районе станиц Каневской и Уманской (35 человек) 
  под руководством студента Рыжова. Отряд состоял из офицеров и демобилизованных 
  красноармейцев. В июне 1923 г. группа Васюка (12 штыков) неудачно пыталась овладеть 
  арсеналом станицы Невинномысской<sup>115</sup>. Все эти выступления были обречены 
  на провал. Летом 1923 г. сдались крупные отряды Комарова, Супрунова, Мордачева, 
  Хорошева и других.<sup>116</sup><br>
  <br>
  Органы ГПУ успешно применили выборочные амнистии; используя противоречия между 
  монархистами и «самостийниками», засылали в отряды агитаторов и лазутчиков. 
  Только за декабрь 1922 г. на Кубани добровольно сдались 94 человек, за июнь 
  1923 г. — около 100 человек<sup>117</sup>. В сентябре 1923 г. был схвачен вожак 
  монархического подполья М.Н. Жуков в окрестностях станицы Батуринской<sup>118</sup>. 
  Поздней осенью разбиты отряды Шумакова и Сапожникова, нанесен урон отрядам Ковалева 
  и Турецкого в Армавирском отделе<sup>119</sup>. Тогда же подавлен однодневный 
  мятеж станицы Зольской на Тереке, не желавшей платить чрезмерный налог. Силы 
  были несоизмеримы, и после жестокого боя прошли массовые репрессии<sup>120</sup>. 
  15 марта 1924 г. в станице Бесстрашной уничтожен отряд Ковалева. Состоялся публичный 
  суд над 69 повстанцами (в том числе из экспедиции Макеева) в Армавире, вынесший 
  смертные приговоры<sup>121</sup>. <br>
  <br>
  Неудивительно, что зажиточный слой казачества продолжал считать власть и ее 
  сторонников чужими для себя. Сословная вражда часто принимала дикие формы, как 
  на хуторе Червонном Армавирского отдела 9 июня 1924 г. Отряд Шершнева подверг 
  пыткам и казнил 24 коммунара-переселенца, а коренные жители станицы Новоалександровской 
  разграбили имущество жертв. Власть ответила жестко. 8 июля члены бюро обкома 
  РКП(б) приказали «политической тройке» полпредства ОГПУ усилить внесудебные 
  расправы, в т.ч. расстрелы<sup>122</sup>. Так замыкался круг нетерпимости и 
  насилия. <br>
  <br>
  Последние проявления вооруженной оппозиции были уничтожены осенью 1924 — весной 
  1925 г. Судя по рапорту уполномоченного ОГПУ в Славянском районе, 31 октября 
  пойман В.Ф. Рябоконь, скрывавшийся с 5-7 казаками в плавнях станицы Гривенской<sup>123</sup>. 
  17 ноября близ станицы Троицкой убит другой влиятельный повстанческий вожак 
  С.С. Дятлов. К маю 1925 г. небольшие группы скрывались у станицы Темнолесской 
  и на Тереке, где и были вскоре разгромлены<sup>124</sup>.<br>
  <br>
  Повстанческое движение «бело-зеленых» на Дону, Кубани, Тереке и Ставрополье 
  — один из примеров консервативной ответной реакции крестьянства на непонятные, 
  враждебные его традиционному укладу жизни модернизаторские усилия власти. Сходство 
  с казачье-крестьянским протестом можно найти в опыте Вандеи. В узком смысле 
  движение «бело-зеленых» было своеобразной частью протеста земледельцев России 
  против «военного коммунизма», отражало поиск и тупики «третьего пути» в российской 
  революции. Повстанческие выступления казаков и в гораздо меньшей степени крестьян 
  Юга России были вызваны сложным комплексом причин: многолетним сословно-этническими 
  и классовыми противоречиями, нерешенностью земельного вопроса, кровавым опытом 
  участия в «большой» Гражданской войне 1917—1920 гг. Основным фактором затягивания 
  вооруженной борьбы стала практика «военного коммунизма», многие сущностные черты 
  которой сохранялись на Северном Кавказе гораздо дольше, чем по стране — вплоть 
  до 1925 г. <br>
  <br>
  Общественная опора «бело-зеленых» — середняцкая толща и зажиточная элита казачества. 
  В Ставропольской и Черноморской губерниях эти социальные функции выполняли те 
  же слои «коренного» крестьянства. Существовали региональные особенности политических 
  ориентаций. Так, терцы и донцы, линейцы Кубани выступали за единую неделимую 
  Россию, за консервативные ценности. Черноморские казаки и крестьяне проявляли 
  сепаратизм и эсеровско- демократические симпатии. Это связано с этнокультурной 
  принадлежностью и обстоятельствами «большой» Гражданской войны. <br>
  <br>
  Стремление земледельцев к миру, разрушительные последствия войн, умелое сочетание 
  репрессий с поощрением лояльности Советской власти привели к постепенной изоляции 
  и краху повстанцев. Вместе с тем своей обреченной борьбой они заставили РКП(б) 
  перейти к НЭПУ, предпринять ряд долгосрочных коренных уступок крестьянству: 
  принять Земельный кодекс, узаконить общину, временно отказаться от насаждения 
  коммун. <br>
