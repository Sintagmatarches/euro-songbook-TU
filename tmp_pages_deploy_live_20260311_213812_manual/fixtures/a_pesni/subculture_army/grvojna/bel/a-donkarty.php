	<br><span class="text"><strong>В. 
        В. Мельникова</strong><br>
  <br>
        <span class="header"><strong>КАРТЫ ДОНСКОЙ АРМИИ 1919 г.</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 67</em>.<br>
  <br>
  <br>
  Картографический материал белой армии периода 1919 г. представлен в книгах из 
  спецхрана научной библиотеки Государственного архива Волгоградской области (ГАВО).<br>
  <br>
  <strong>«Отчет командующего Донской армией по оперативной части к концу сентября 
  1919 года. Краткий обзор вооруженной борьбы Дона с советской властью за время 
  от февраля по октябрь 1919 года». Новочеркасск, 1919. </strong><br>
  <br>
  Книга издана в 1919 г. в областной типографии Всевеликого войска Донского в 
  Новочеркасске. Имеет гриф «Секретно». Тираж книги не указан (имеет номер 295 
  на обложке, мягкий переплет), не переиздавалась. «Краткий обзор...» составлен 
  генерал-квартирмейстером штаба Донской армии, Генерального штаба полковником 
  Кисловым и начальником оперативного отделения Генерального штаба полковником 
  Добрыниным 25 сентября 1919 г. в г. Миллерово (имеются подлинные подписи в экземплярах 
  книги с твердым переплетом, но без карт). К тексту прилагаются картографические 
  материалы в количестве 4 карт издания топографического отделения штаба Донской 
  армии. <br>
  <br>
        <strong>Схема №1. «Положение сторон к 21 апреля 1919 года»</strong><br>
  <br>
  Схема дана на карте округов области войска Донского с грифом «Секретно». Масштаб 
  карты: 25 верст (верста — 1,06 км) в дюйме (дюйм — 2,54 см). Размер карты: 67,5х53,5 
  см. «Печатано IV 1919 г.». Указаны силы сторон: <br>
  <br>
  Донской армии: <br>
  бойцов – 15000<br>
  орудий - 108<br>
  пулеметов - 411<br>
  <br>
  Противника: <br>
  бойцов – 115250<br>
  орудий - 538<br>
  <br>
  По армиям: <br>
  VIII армия<br>
  14250 бойцов<br>
  IX армия<br>
  38000 бойцов<br>
  115 орудий<br>
  Х армия <br>
  25000 бойцов<br>
  263 орудия<br>
  XIII армия <br>
  15000 бойцов<br>
  66 орудий<br>
  Группа соединения армий <br>
  11000 бойцов <br>
  20 орудий <br>
  Группа войск Харьковского направления <br>
  12000 бойцов <br>
  20 орудий <br>
  <br>
        <strong>Схема №3. «Положение сторон к 1 августа 1919 года»</strong><br>
		<br>Схема 
        дана на карте округов области войска Донского, Саратовской и Тамбовской 
        губерний с грифом «Секретно». Масштаб карты: 25 верст в дюйме. Размер 
        карты: 60,5х57,5 см. Указаны силы сторон: <br>
  <br>
  Донской армии: <br>
  бойцов – 41400<br>
  орудий – 176<br>
  пулеметов - 794<br>
  <br>
  Противника: <br>
  бойцов - 76250 <br>
  орудий — 304 <br>
  <br>
  По армиям: <br>
  VIII армия <br>
  40750 бойцов<br>
  105 орудий <br>
  IX армия <br>
  15700 бойцов<br>
  97 орудий <br>
  Х армия <br>
  18000 бойцов <br>
  102 орудия <br>
  XIII армия <br>
  19000 бойцов <br>
  75 орудий <br>
  XIV армия <br>
  23000 бойцов <br>
  70 орудий <br>
  <br>
  Обозначен 2-ой рейд генерала Мамантова и фронт Добровольческой армии. <br>
  <br>
        <strong>Приложение к схеме №4. «Положение сторон к 25 сентября 1919 года»</strong><br>
  <br>
  Масштаб карты: 
  100 верст в дюйме. Указаны силы сторон: <br>
  <br>
  Противника: <br>
  бойцов — 93000 <br>
  орудий - 402<br>
  <br>
  По армиям: <br>
  VIII армия<br>
  20000 бойцов<br>
  100 орудий<br>
  IX армия <br>
  30000 бойцов<br>
  170 орудий<br>
  Х армия <br>
  37000 бойцов <br>
  132 орудия <br>
  <br>
  Обозначен 3-ий рейд генерала Мамантова, фронт Добровольческой и Донской армий. 
  Таким образом, как указано в отчете, <br>
  <br>
  «4. В последний период до 20-х чисел сентября блестящим, беспримерным в истории 
  рейдом конницы генерала Мамантова расстроен центр всего Южного фронта красных, 
  чем облегчено продвижение правого фланга Добровольческой армии, в свое время 
  прикрывшей грудью левый фланг Донской армии в Бахмутском районе, и левого фланга 
  Донской. <br>
  <br>
  5. Этим же рейдом уничтожены неподдающиеся учету склады боевых припасов, интендантские 
  склады, артиллерийские, распущено более 100000 мобилизованных, которые понесли 
  с собой по всей России весть, за что борется донское казачество; из этого же 
  рейда в Донскую армию приведена целая пехотная дивизия (Тульская), уже оказавшая 
  помощь при взятии Воронежа и Коротояка». <br>
