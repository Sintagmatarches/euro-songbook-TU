        Публикация П.А. Новикова<br>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 225</em>.<br>
        <br>
  <br>
  г. Чита. 17 июня [1920 г.]<br>
  <br>
  По преемственности Российской Верховной власти на территории Российской Восточной 
  окраины от Верховного правителя адмирала А.В. Колчака и в согласии с Постановлением 
  Совещания башкир некоторых уездов Башкирии (Приуральского района), состоявшегося 
  14-16 июня 1920 года, я, Главнокомандующий всеми вооруженными силами и Походный 
  атаман всех казачьих войск Российской Восточной окраины, объявляю: <br>
  <br>
  1. Башкир, находящихся на территории РВО, полагать на основаниях, установленных 
  для казаков. Присвоив им права и обязанности применительно к таковым Оренбургского 
  казачьего войска. <br>
  <br>
  2. Войсковым органам башкир на правах войсковых правлений и войсковых штабов 
  казачьих войск почитать Военно-Национальное Управление башкир, которому разработать 
  соответственно географическому, историческому и национальному положению Башкирии 
  и представить мне на утверждение Положение о башкирском войске. <br>
  <br>
  3. Окончательное разрешение вопроса о восстановлении за башкирами казачьих прав 
  и разработку окончательного о них Положения представить на усмотрение Всебашкирского 
  съезда, который может быть создан при очищении двух третей территории Башкурдистана. 
  <br>
  <br>
  4. Идя навстречу желаниям башкир РВО, я счел приемлемым взять на себя покровительство 
  над ними, включив Башкирское войско в число казачьих и инородческих войск, возглавляемых 
  мною как Походным атаманом и управляемым через Штаб Походного атамана. <br>
  <br>
  Объявляя сию Грамоту, уверен, что права, казачеству присвоенные, будут твердой 
  опорой для башкир, кои, сплотившись, воедино, создадут крепкий духом и здоровый 
  государственный оплот для дальнейшей борьбы за восстановление нашей общей Родины 
  — России. <br>
  <br>
  Генерал-лейтенант, атаман Семенов <br>
