	<span class="text"><strong>О.В. Кондрашенко</strong><br>
  <br>
        <span class="header"><strong>КОНСТИТУЦИОННЫЙ ОПЫТ ГОСУДАРСТВЕННОГО 
        СТРОИТЕЛЬСТВА НА ТЕРРИТОРИИ КАЗАЧЬИХ ВОЙСК ЮГО-ВОСТОКА РОССИИ В ПЕРИОД 
        ГРАЖДАНСКОЙ ВОЙНЫ</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 33-37</em>.<br>
  <br>
  <br>
  Возникновение нового государства при развале привычных системных связей в рамках 
  имперской структуры всегда является многофакторным и очень сложным процессом. 
  Общие политологические исследования последних лет по вопросам кризисных тенденций 
  в государственных образованиях показывают, что принципиальной удачей здесь является 
  ответ на вопрос: возможно ли создать на развалинах империи государства современного 
  типа, способные к устойчивому существованию в мире горизонтальных связей. Под 
  этим подразумевается, что рыночно-экономические, технологические, культурно-коммуникационные 
  связи преобладают над вертикальным административным управлением из центра<sup>1</sup>. 
  С одной стороны, усиление горизонтальных связей и ослабление зависимости частей 
  ют центра, возрастание их самостоятельности и, самое главное, осознание особенностей 
  исторического развития, усиление самобытности и значимости, приводящих, в конце 
  концов к распаду империи, как бы сами по себе свидетельствуют о возможности 
  рождения новых государственных организмов. Однако сущностная перспектива их 
  эволюции заключается здесь в том, что в государстве современного типа (то есть 
  одного из вариантов, возникших на рубеже 1920-40-х гг.) силы внутренней интеграции 
  должны преобладать над формальными предпосылками процесса государственного строительства. 
  <br>
  <br>
  Обращаясь к теме казачьей государственности, следует отметить, что определить 
  адекватность провозглашенных государственных программ политической зрелости 
  тогдашних донского, кубанского и терского обществ достаточно трудно. Многие 
  компоненты и институты государства, в его современном понимании, либо не достигли 
  тогда зрелости, либо вовсе отсутствовали, если речь, например, шла о важнейшей 
  составляющей процесса государственного становления, которую ныне принято соотносить 
  с понятием государства-нации<sup>2</sup>. В связи с этим при исследовании государственных 
  образований периода Гражданской войны целесообразно использовать функциональный 
  подход, когда государство предстает в виде относительно жестко закрепленной 
  институциональной основы политической системы. <br>
  <br>
  При такой трактовке государство логично исследовать с правовой и организационно-структурной 
  стороны, выявляя конкретную технику организационной власти. Такой подход позволяет 
  оценить если не функциональную зрелость государственных структур, то, по крайней 
  мере, их организационную завершенность, что немаловажно при анализе перспектив 
  жизнеспособности любого государственного организма вообще и казачьих государственных 
  образований юго-востока России, в частности. <br>
  <br>
  Процессы распада Российской империи в 1917 г. получили толчок в связи с исчезновением 
  связующего властного центра. В итоге нарастающие центробежные тенденции приводили 
  либо к активизации сил, выступающих за самоопределение окраин, либо к особой 
  организации власти областной. В первом случае, очевидно, срабатывали механизмы 
  единения национальных сообществ. Этнический и духовный факторы укрепляли местное 
  «мы», которое оказывалось сильнее эфемерной «общероссийской» идеи. Попытка дистанцироваться 
  от центра приводила к изменению статуса и функций областной власти, которая 
  принимала на себя государственные полномочия. <br>
  <br>
  Обозначенные тенденции в казачьих областях юго-востока России приобрели своеобразную 
  специфику не в последнюю очередь из-за особого статуса казачьих войск в прежней 
  Российской империи. К концу 1917 г. по крайней мере, два из трех казачьих войск, 
  а именно Донское и Кубанское, оформились во вполне самостоятельные политические 
  образования с элементами государственности в виде функционировавших уже к этому 
  моменту систем управления. В особой ситуации оказалось Терское войско. Процессы 
  политического самоопределения продолжались здесь очень недолго. После гибели 
  13(26) декабря 1917 г. терского атамана М.А. Караулова Временное Терско-Дагестанское 
  правительство фактически перестало функционировать. Лишь в 1919 г. после освобождения 
  области от большевиков восстановились казачьи структуры самоуправления, и Терское 
  войско вышло на сцену антибольшевицкой борьбы как самостоятельная сила. <br>
  <br>
  Процессы государственного строительства, происходившие на казачьих территориях 
  в 1917-1919 гг. имели разную степень зрелости, но все они объединялись, во-первых, 
  исторической преемственностью традиций власти и управления, во-вторых, соблюдением 
  процедур легитимации и легализации власти. Иными словами в 1917 г. на территориях 
  казачьих войск возродились представительные органы власти в лице Кругов, Войсковые 
  атаманы проходили процедуру избрания на пост, принимались своеобразные казачьи 
  конституции», документы, фиксировавшие основные принципы и положения устройства 
  власти и территории, выстраивалась традиционная для казаков вертикаль власти. 
  <br>
  <br>
  На Дону к началу ХХ в. сохранились, хотя и в измененном виде, институт атаманства 
  и Войсковой Круг. Конечно, их роль в решении внутривойсковых вопросов стала 
  довольно призрачной. Имперская власть в течение нескольких столетий планомерно 
  урезала автономные права Области войска Донского. Впервые за длительный период 
  времени самостоятельное определение направления государственно-политического 
  развития войска Донского, было сделано Первым Большим Войсковым Кругом (БВК), 
  собравшимся 26 мая (8 июня) 1917 г. в Новочеркасске. <br>
  <br>
  БВК разработал и утвердил основные положения по управлению областью. Самыми 
  существенными из них стали, во-первых, возврат к выборному статусу Войскового 
  атамана в противовес назначению его центральной властью и, во-вторых, объявление 
  Круга единственным правомочным органом на территории Области войска Донского<sup>3</sup>. 
  Донскому областному исполнительному комитету, как российскому органу было фактически 
  запрещено вмешиваться в казачьи дела, а исполкомы низовых уровней (хуторские, 
  станичные и, даже, окружные) на территории войска вообще упразднялись<sup>4</sup>. 
  На БВК была предложена и схема казачьего управления, состоявшая из вертикали 
  хуторского, станичного и областного управления. В этой области законотворчества 
  Круга парадоксальным образом переплелись «дух традиции» и «дух нового времени». 
  Предложенная схема была традиционной для донского казачества, но основным принципом 
  ее формирования объявлялась выборность управленческих структур на демократической 
  платформе «всеобщего равного тайного и прямого права» с наделением избирательными 
  правами женщин и неказачьего населения области. <br>
  <br>
  После прекращения деятельности Временного правительства местная казачья власть 
  приняла на себя статус государственной. Донской атаман А.М. Каледин заявил после 
  октябрьских событий в Петрограде о создании соответствующих общероссийским государственных 
  органов управления в лице атамана и правительства. Так, в общем-то, и были заложены 
  правовые основы донской государственности. <br>
  <br>
  Начало 1918 г. характеризовалось разрушением исторически сложившегося политического 
  и гражданского управления областью вследствие прихода большевиков. Новый виток 
  государственного строительства на Дону начался после антибольшевицкого восстания. 
  Начался он опять-таки с созыва Круга. 28 апреля (11 мая) 1918 г. в Новочеркасске 
  собрался так называемый «Круг Спасения Дона», который должен был заново сформулировать 
  основные принципы жизнедеятельности казачьей области. Круг был созван, несмотря 
  на то, что область к этому моменту уже имела фактическое правительство. «Совет 
  обороны» или по-другому Временное Донское правительство (это название появилось 
  чуть позже) самоорганизовалось в разгар антибольшевицкого восстания на Дону 
  и оно же само ограничило период своего правления датой созыва Круга<sup>5</sup>. 
  Первое, что сделал Круг — это провел процедуру собственной легитимации. Участники 
  форума без долгих прений признали число делегатов (их было всего 130 человек 
  от Черкасского и I-го Донского округов) достаточным и собрание получило все 
  полномочия законодательного органа<sup>6</sup>. Однако, понимая непрочность 
  собственных позиций, делегаты объявили, что «Круг Спасения» наделяется всей 
  полнотой власти впредь до созыва БВК, который планировали созвать через два 
  месяца по окончании работы «Круга Спасения». <br>
  <br>
  Встречные тенденции государственного строительства на Дону прослеживались в 
  деятельности П.Н. Краснова, избранного 3 (16) мая 1918 г. на пост атамана войска 
  Донского. Краснов, будучи яркой политической личностью, стал фактическим создателем 
  системы государственного управления на Дону. Он продиктовал Кругу «Основные 
  законы Всевеликого Войска Донского» и поставил условием своего вступления в 
  должность их безоговорочное принятие. Между тем «Основные законы...» представляли 
  собой практически копию «Основных законов Российской империи» от 23 апреля 1906 
  г. и, соответственно, подчеркивали либо исключительные полномочия Войскового 
  атамана, либо его ведущую и координирующую роль (в зависимости от сферы приложения 
  власти). Об этом говорилось приблизительно в 15 из 50 статей «Основных законов...». 
  <br>
  <br>
  Восемь разделов казачьей «конституции» охватывали основные сферы государственного 
  строительства. На первом месте шло положение об атаманской власти. Идеологическим 
  компонентом «конституции» являлись постоянные апелляции Краснова к казачьей 
  старине, традициям. Понимая, что именно на казаков легла основная тяжесть борьбы, 
  Краснов пытался всячески укрепить их дух и желание бороться. Здесь он пускал 
  в ход любые средства, от создания своего флага, гимна, возрождения герба и якобы 
  старинного названия «Всевеликое Войско Донское», до закрепления за рядовым казачеством 
  права владения им землей и исключения возможности земельного передела в пользу 
  иногороднего, крестьянского элемента<sup>7</sup>. <br>
  <br>
  Ни слова в «конституции» не было сказано о «хозяине земли Донской» Круге. Таким 
  образом, несмотря на звучавший рефреном в речах Краснова пиетет по отношению 
  к этому традиционному органу казачьей власти, фактически Круг был отстранен 
  от руководства политической жизнью Войска. В своих действиях Краснов не был 
  подотчетен никому. На атамана даже не возлагалась ответственность за ход управления 
  войском. Управляющих отделами и председателя Совета управляющих Краснов назначал 
  сам, так же как и членов Войскового суда. В результате, правительство превратилось 
  в обезличенный рабочий кабинет атамана. Отчасти атаману удавалось влиять даже 
  на судебную систему войска. При осуществлении такой политики, подкрепленной 
  законодательно, в войске вскоре де-факто произошло слияние функций законодательной 
  и исполнительной власти. До созыва БВК Краснов оставался, практически, единоличным 
  правителем нового государственного образования. Такой властью не обладали ни 
  Каледин, ни сменивший его А.М. Назаров (расстрелянный впоследствии большевиками). 
  По впечатлениям современников период правления Каледина был, скорее, периодом 
  коллективного руководства, когда «первый донской Войсковой Круг дал пернач (знак 
  власти — О.К.) выборному атаману, но не дал ему власти», а во главе Войска встало 
  войсковое правительство из 14 старшин, избранных вне всякой зависимости от их 
  государственного, общественного и просто делового стажа<sup>8</sup>.<br>
  <br>
  Таким образом, вся конструкция государственной власти была выстроена Красновым 
  под себя. Политика государства находилась в зависимости от позиции его главы. 
  Краснова называли «самодержцем демократического казачества». Однако сам же «самодержец» 
  признавал, что не в силах навязать казачеству свою волю в решении самого главного 
  вопроса — защиты области от большевиков. Донцы категорически отказывались выходить 
  за пределы области. Краснов смог отдать приказ о выдвижении Донской армии за 
  границы области и о занятии таких городов, как Царицын, Камышин, Балашов, Новохоперск 
  и Калач только 29 августа (11 сентября) 1918 г. в разгар заседаний БВК, подкрепив 
  свой приказ авторитетом этого органа<sup>9</sup>. <br>
  <br>
  Вехой в развитии Донской государственности стал БВК, собравшийся 15 (28) августа 
  1918 г. в Новочеркасске. Работа Круга сосредоточилась на изменении основных 
  пунктов донской «конституции» и конструировании такой модели управления в Войске, 
  которая заведомо бы исключала колебания политического курса и ставила бы исполнительную 
  власть под контроль законодательной. Переработанные положения «Основных законов» 
  теперь значительно изменяли взаимоотношения базовых структур власти. <br>
  <br>
  Новая донская «конституция» разрабатывалась гораздо более профессионально, чем 
  в апреле-мае, что чувствовалось с первых же ее пунктов. Наконец-то был оформлен 
  юридически основополагающий принцип построения властных отношений в новом государстве 
  — «народоправство», специально выделялся пункт о республиканской форме государственного 
  строя. Верховная законодательная власть вручалась Кругу, высшая исполнительная 
  — Атаману, но при соблюдении парламентской и уголовной ответственности последнего 
  перед Кругом. Высшая судебная власть отдавалась судебным установлениям (т.е. 
  учреждениям — О.К.) и лицам, осуществляющим ее именем закона<sup>10</sup>. Среди 
  институтов новой власти появился новый орган — Донской правительствующий Сенат. 
  <br>
  <br>
  В отличие от майских «Основных законов...», августовская «конституция» довольно 
  подробно расписывала сферу деятельности того или иного органа власти. Специальный 
  ее раздел, к примеру, посвящался полномочиям Круга как высшей законодательной 
  власти. Перераспределение властных полномочий между законодательной и исполнительной 
  властью привело к тому, что депутаты Круга попытались, насколько это возможно, 
  урезать сферу деятельности донского атамана. Из предмета ведения атамана законодательно 
  исключались: прерогатива заключения мира и объявления войны, утверждение бюджета 
  и рассмотрение финансовой политики государства. Против него могло возбуждаться 
  уголовное преследование по постановлению 2/3 голосов наличных членов Круга<sup>11</sup>. 
  В дополнение ко всему законодательная власть лишила атамана возможности контролировать 
  финансы области<sup>12</sup>. Все финансовые вопросы находились теперь в поле 
  деятельности Круга. <br>
  <br>
  Рамки деятельности Совета управляющих, напротив, значительно расширились. Этот 
  орган теперь имел право составлять законопроекты и издавать законы в перерывах 
  между сессиями Круга<sup>13</sup>. Законодательная инициатива атамана сохранилась 
  в границах права утверждать законы и возвращать их на доработку, но если закон 
  принимался Кругом вторично, то его вводили в действие без промедления<sup>14</sup>. 
  Таким образом, прежде всеохватывающий спектр деятельности атамана после БВК 
  функционально изменился в сторону большей специализации в результате оттока 
  власти по другим каналам. <br>
  <br>
  Новая «конституция» вызвала бурные споры между атаманом и оппозиционерами. Причиной 
  спора стали противоречия в построении концепции власти. Одна сторона (Круг) 
  склонялась к коллегиальной ее конструкции, тогда как другая (Атаман) настаивала 
  на форме единоначалия. Поводом к началу ожесточенной полемики послужил вопрос 
  об учреждении окружных кругов<sup>15</sup>. По мнению атамана, введение в структуру 
  власти окружных законодательных собраний усиливало опасность нарастания сепаратистских 
  тенденций и, в конечном счете, раскола Войска<sup>16</sup>. Тем не менее, окружное 
  звено власти все же появилось. <br>
  <br>
  Созданная на Большом Войсковом Круге структура власти, в результате перераспределения 
  властных полномочий «наверху», в значительной степени стала напоминать парламентскую 
  систему. Повышение статуса Круга и расширение инициативы правительства, с одновременным 
  сужением поля деятельности главы государства, поставили атамана перед необходимостью 
  взвешенного сотрудничества с остальными ветвями власти. Личное же положение 
  Краснова стало нестабильным. С ослаблением военных успехов Донской армии, а 
  так же материальной и политической поддержки германского командования значительно 
  понизился имидж и политическое влияние Краснова на жизнь антибольшевицкого Дона. 
  <br>
  <br>
  Таким образом, в 1918 г. на Дону полным ходом шло строительство основ государственности. 
  Однако, несмотря на объявление о суверенном характере Всевеликого войска Донского, 
  Краснов достаточно легко соглашался с Деникиным при обсуждении вопросов по объединению 
  судебной системы, железных дорог и т.п., но всеми силами противился объединению 
  военного командования с «добровольцами». Тем более, что речь шла не просто об 
  объединении, а о подчинении донского командования Деникину. <br>
  <br>
  Несмотря на то, что по условиям соглашения с Деникиным, достигнутого в ходе 
  переговоров на станции Торговая, достояние донских казаков (их земли, недра, 
  условия быта и службы Донской армии) не затрагивалось<sup>17</sup> фактически 
  войско попадало под контроль деникинской администрации. Конституция войска переходила 
  из сферы гаранта прав суверенного государства в область правовой базы для местного 
  самоуправления, не поднимавшегося выше уровня автономии. <br>
  <br>
  После ухода Краснова с поста атамана 2 (15) февраля 1919 г. очень быстро пошел 
  процесс втягивания войска в орбиту влияния армии Деникина. Добровольческая военная 
  администрация начала подавлять свободу действий казачьей власти. Провозглашаемая 
  в казачьих областях, в том числе и на Дону, свобода печати игнорировалась военными 
  властями ВСЮР. Для многих видных деникинцев идеалом взаимоотношений с казачьими 
  правительствами было либо упразднение распоряжений и установлений последних, 
  либо полное подчинение и ответственность таковых «перед Всероссийским диктатором»<sup>18</sup>. 
  К сентябрю 1919 г. можно уже было утверждать, что многие созидательные проекты 
  государственных деятелей автономистов ВВД оказались подчинены руководству деникинских 
  политиков. <br>
  <br>
  Схожие с донскими события происходили и на Кубани, где в апреле 1917 г. по инициативе 
  Временного областного исполнительного комитета собрался общеобластной съезд. 
  Целью форума стало создание управленческого комплекса, объединившего бы казачьи 
  и российские органы власти. Естественно, за основу брался российский образец. 
  В итоге законодательным органом власти становился Областной совет, исполнительным 
  — Исполком. Казачьи учреждения управления и самоуправления сохранялись как особый 
  элемент областной управленческой системы в виде Войскового правительства и Войскового 
  совета. Такой проект явно содержал в себе опасность раскола. <br>
  <br>
  На следующий день после закрытия съезда, 18 апреля (1 мая) 1917 г., собралась 
  казачья секция съезда, объявившая себя Кубанской Войсковой Радой. Официальный 
  разрыв с пророссийским кубанским Областным советом произошел на первой же сессии 
  этого органа, начавшейся 24 июня (7 июля) 1917 г. В ночь с 3 (16) на 4 (17) 
  июля Войсковой совет объявил о принятии функций Областного совета на себя, а 
  Войсковое правительство было объявлено высшей исполнительной властью. <br>
  <br>
  Легитимные основания для новой власти были сформулированы в речи председателя 
  правительства А.П. Филимонова, Будущий атаман Кубанского войска заявил: «Мы 
  вынуждены были взять власть в руки, чтобы она не попала к иногородним»<sup>19</sup>. 
  Для обеспечения собственной устойчивости казачья власть на Кубани так же, как 
  и на Дону, апеллировала к войсковой традиции сохранения и укрепления казачьих 
  прав и привилегий. <br>
  <br>
  В сентябре 1917 г. вторая Краевая рада провозгласила себя органом всего Кубанского 
  края и приняла положение об органах верховной власти<sup>20</sup>. Из Краевой 
  Рады выделилась Законодательная Рада, а 12 (25) октября 1917 г. на пост атамана 
  избрали полковника А.П. Филимонова. Войсковое правительство сохранило свои позиции 
  в структуре властных органов Кубанского края. На этом же заседании Рады принимается 
  Основной закон о положении и управлении Кубанским краем. Согласно положениям 
  Основного закона Войсковой атаман, избираемый Радой, являлся представительной 
  фигурой власти. Он должен был согласовывать свои приказы с председателем правительства 
  или представителем того ведомства правительства, которого данный приказ касался. 
  Председатель правительства назначался Радой и все правительство также формировалось 
  Радой, атаман лишь подписывал приказ о его назначении<sup>21</sup>. <br>
  <br>
  По основному закону Кубанская область именовалась Кубанским краем, Войсковое 
  правительство — Краевым, а Войсковая Рада — Краевой. Таким образом, Кубань получала 
  не войсковой статус, а краевой, что делало ее более открытой для общероссийской 
  системы. Однако, в рассматриваемый период времени на Кубани, так же как и на 
  Дону в политике все настойчивее проявлялась тенденция к обособлению от центральной 
  российской власти. 5 (18) октября 1917 г. Рада приняла постановление о выделении 
  края в самостоятельную Кубанскую республику. 26 октября кубанское правительство 
  так же, как и донское приняло на себя функции государственной власти. Суверенный 
  характер этой власти был условным и объяснялся, прежде всего, желанием оградить 
  Кубань от волны анархии, которая шла их столичного центра. 20 декабря 1917 г. 
  (2 января 1918 г.) Краевая Рада утвердила политическую программу кубанского 
  казачества и горцев. Эта программа являла собой попытку закрепить расширенные 
  прерогативы местной власти. В программе говорилось о подчиненности казачьих 
  частей только своим Войсковым атаманам. Привлечение казаков к военной службе 
  вне территории Кубанского края стало возможным только с разрешения Войскового 
  правительства. Кубанское войско объявлялось единственным самостоятельным и независимым 
  распорядителем всего того, что составляло неотъемлемую собственность Кубанского 
  войска: земель, лесов, и рыбных ловель<sup>22</sup>. <br>
  <br>
  Таким образом, в 1917 г. на начальном этапе формирования власти Дон и Кубань 
  действовали в унисон. Они практически одновременно заявили о своем отделении 
  от центра и постарались максимально расширить полномочия местной власти. Но 
  на открытый разрыв с Россией не пошли, постоянно заявляя о своем единстве с 
  ней. <br>
  <br>
  Вместе с тем, эволюция донской и кубанской государственности существенно отличалась 
  друг от друга. Причиной тому был ряд внутри- и внешнеполитических факторов. 
  В отличие от Дона, территория которого, в частности, северные округа, время 
  от времени становилась театром военных действий, Кубань с 29 октября (11 ноября) 
  1918 г. являлась тыловой областью Вооруженных сил на Юге России (по характеристике 
  Деникина). Впоследствии Кубань более года находилась в состоянии относительного 
  покоя и ее власти занялись организацией управления и разработкой базовых принципов 
  власти. <br>
  <br>
  Правовое оформление кубанская государственность получила с принятием 5(18) декабря 
  1918 г. «Временного положения об управлении Кубанским Краем» - конституции Кубани, 
  При этом, как отмечал один из видных политических деятелей Края Д.Х. Скобцов, 
  в конструкции кубанской государственности конца 1918 г. четко прослеживалась 
  преемственность с тем, что было заложено в ее фундамент еще в 1917 г. Особое 
  внимание, по-прежнему, уделялось организации ведущих элементов политической 
  системы — Раде, правительству и институту атаманской власти. Значительным новшеством 
  в конструировании кубанской государственности в этот период стало принятие конституции. 
  Источником высшей власти в Кубанском крае объявлялась воля его граждан, при 
  этом краевой принцип организации Кубани давал властям возможность привлекать 
  к управлению и такие неказачьи группы населения как горцев и коренных крестьян<sup>23</sup>. 
  Сохранение демократических основ государственности должен был гарантировать 
  принцип разделения властей, заложенный в основу кубанской системы управления. 
  При этом коллегиальный принцип руководства являлся основополагающими для кубанской 
  государственности. Это проявилось и в организации законодательного органа власти 
  - Рады. С 1917 г. в Крае сохранилось разделение на Законодательную и Краевую 
  Раду. Разрабатывала законы Законодательная Рада, а утверждала их Краевая, то 
  есть, текущая политика на Кубани находилась под двойным контролем, в том числе 
  и деятельность правительства и атамана. Сфера полномочий главы исполнительной 
  власти в кубанской «конституции» представлялась крайне ограниченной. Атаман 
  мог лишь один раз возвращать в Законодательную Раду законы на предмет их доработки. 
  Существовало положение, при котором наличие вопросов определенного характера 
  позволяло Раде не допускать атамана на их обсуждение. И, наконец, по конституции 
  существовала определенная взаимозависимость атамана и председателя правительства, 
  когда издаваемые акты одного обязательно скреплялись подписью другого. В этом 
  кубанская «Конституция» существенно отличалась от донских «Основных законов...», 
  особенно от их майской редакции 1918 г. где четко выделялся принцип диктатуры 
  первого лица — Атамана. <br>
  <br>
  В целом, политико-государственная идеология на Кубани выглядела более последовательной, 
  нежели на Дону, где смена политических ориентиров в руководстве с Диктатуры 
  на парламентаризм усугубила кризис политического режима. Вместе с тем, при реализации 
  государственнических проектов в смысле организации суверенной власти, кубанские 
  политики, в отличие от донских, находились под постоянным контролем деникинской 
  администрации. <br>
  <br>
  В гораздо более стесненных, чем на Дону и Кубани, условиях формировалась терская 
  казачья государственность. Терский Большой Войсковой Круг собрался спонтанно, 
  сразу после очищения территории Терского войска от большевиков. На Круге присутствовали 
  делегаты от всех четырех отделов войска. Делегаты группировались не по политическому, 
  а по территориальному принципу. Очевидно в связи с этим Круг, подобно «Кругу 
  Спасения Дона» не имел ни четко выраженного политического лица, ни рабочей программы. 
  Предложения делегатов нередко заостряли внимание на второстепенных вопросах, 
  например на месте пребывания Войскового атамана и правительства. Более серьезные 
  вопросы об экономической и аграрной политике, о Юго-Восточном Союзе включили 
  только в повестку дня четвертого заседания 23 февраля (8 марта) 1919 г. <br>
  <br>
  В качестве основополагающих для политического бытия Терского войска можно выделить 
  три поставленных на Круге вопроса: выбор атамана и принятие конституции, Юго-Восточный 
  Союз и соединение с Кубанским войском «для устроения крепкого, прочного тыла...» 
  в деле воссоздания «единой мощной великой России»<sup>24</sup>. Конституция 
  Терского войска, опираясь на принципы демократии, как и на Дону, провозглашала 
  свободу совести, слова, печати, собраний и союзов. Вопрос об атаманской власти 
  рассматривался отдельно в «Основаниях», принятых 27 февраля (12 марта) 1919 
  г. В целом, многие основополагающие в деле государственного строительства аспекты 
  отличались существенной недоработкой. Например, сфера деятельности каждой ветви 
  власти не оговаривались, отсутствовало положение о Войсковом Круге. Идея казачьего 
  суверенитета сохранялась в немногих положениях о Войсковом атамане и правительстве, 
  которым принадлежала вся пол- нота исполнительной власти в войске. Система высших 
  органов власти дополнялась положением о том, что «судебная власть осуществляется 
  независимыми судьями», а также таким органом как Малый Круг, работавшим в перерывах 
  между сессиями БВК и занимавшимся разработкой законов, утверждаемых впоследствии 
  БВК. Все высшие органы власти, включая атамана, правительство и Малый Круг, 
  в своей деятельности должны были следовать законам Временного правительства 
  от 28 марта (10 апреля) 1917 г. Что же касается органов низового самоуправления, 
  в лице атаманских отделов, станичных сборов и станичных команд, то им предписали 
  руководствоваться положением 1891 г. и этим ограничились до конкретизации данных 
  положений в будущей конституции». Окончательно основной закон Терского войска, 
  а так же положения об атамане и Малом Круге приняли лишь во вторую сессию БВК 
  2(15) июля 1919 г. <br>
  <br>
  Правовая разработанность основополагающих принципов государственного строительства 
  в Терском войске стояла на порядок ниже, чем в остальных казачьих войсках. Это 
  объясняется рядом объективных условий существования этого войска. На протяжении 
  всего 1918 г. территория Терека находилась под контролем большевизированных 
  Советов и только в начале 1919 г. была освобождена войсками ВСЮР, что обусловливало 
  зависимость терцев в решении многих вопросов от руководства Вооруженных сил 
  на Юге России. И, конечно же, серьезно ощущалась нехватка времени для основательной 
  разработки правовой базы терской государственностии. <br>
  <br>
  Впрочем, этот фактор являлся основополагающим и для донской и для кубанской 
  государственности. В целом же следует признать, что в условиях гражданской войны 
  главным гарантом независимости для государственных образований становились армии, 
  а не конституционные конструкции политиков-правоведов. В итоге военный фактор 
  сводил на нет усилия политиков по созданию государственно-политических систем 
  на казачьих территориях при неразрешенности проблем военного противостояния 
  большевицких и антибольшевицких сил. <br>
