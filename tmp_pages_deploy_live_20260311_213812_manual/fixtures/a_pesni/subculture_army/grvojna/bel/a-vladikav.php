	<span class="text"><strong>Ф.С. Киреев</strong><br>
  <br>
        <span class="header"><strong>ВЛАДИКАВКАЗСКАЯ ОПЕРАЦИЯ</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 107-114</em>.<br>
  <br>
  <br>
  В длинной череде событий, потрясших Терек в 1918 г., восстание терских казаков 
  против Советской власти занимает особое место. Восстанием была охвачена почти 
  вся Терская область и соседние уезды Ставропольской губернии и оно имело характер 
  народной борьбы. В советской историографии это восстание называют выступлением 
  и именуют не иначе как «бичераховщина», по фамилии одного из его организаторов 
  — инженера Георгия Бичерахова. <br>
  <br>
  Казачье восстание на Тереке во многом было подготовлено самой Советской властью, 
  с самого начала зачислившей более чем 200-тысячное казачье население Сунжи и 
  Терека в стан врагов. Активно проводившийся развал войсковых структур, намеки 
  на возможный предел земли, а затем и узаконенное переселение и даже уничтожение 
  станиц, заигрывание с горцами и иногородними, претендовавшими на казачьи земли 
  - все это совсем не прибавляло симпатий казаков к новой власти. С начала 1918 
  г. в станицах почти открыто стало проявляться недовольство. Столкновения с горцами 
  на Сунже перерастали в настоящую войну и если с этой бедой, объединившись, можно 
  было хоть как-то справиться, то с дезертирами, хлынувшими в область с Кавказского 
  фронта, бороться казакам было гораздо труднее. Тысячи крупных и мелких отрядов 
  этих вечно пьяных «бойцов», расползаясь по области, грабили, жгли, насиловали 
  и убивали. <br>
  <br>
  Стычки с дезертирами и горцами держали терцев в постоянном напряжении. Разобщенность 
  и растерянность казачества, вызванная антивоенной агитацией и усталостью от 
  войны, давали возможность большевикам без особых усилий брать власть на местах 
  в свои руки. Виртуозно проведенный Кировым съезд народов Терека в Пятигорске, 
  законодательно оформивший торжество власти Советов, окончательно убедил терское 
  казачество в том, что нужно надеяться только на себя. Приехавшие со съезда делегаты 
  рассказывали в станицах, как жестоко отстаивали свою позицию горцы и как лебезили 
  перед ними союзники из «левого» блока, готовые на любые уступки ради удержания 
  власти. Об иногородних и говорить нечего - надеясь на передел казачьих наделов, 
  они были готовы идти за кого угодно, громче других ругая казаков за «отсталость 
  и тайную контрреволюцию». <br>
  <br>
  По станицам шел ропот. В то время, как на Сунже разгорались бои казаков с ингушами, 
  в Пятигорском отделе за три дня с помощью полуанархистских банд, силой разоружили 
  половину станиц. Вслед за этим начались насильственные реквизиции, сопровождавшиеся 
  убийствами и грабежами, затем стали расстреливать офицеров и атаманов. По станицам 
  Пятигорского и Моздокского отделов поскакали гонцы: «Ждите, братья, скоро наступит 
  час». Громко заговорили о полковнике Шкуро, собравшем в Бургустанском лесу казаков, 
  об отрядах Агоева<sup>1</sup> и Гажеева<sup>2</sup>, безжалостно громивших дезертиров 
  и матросов. Казачье восстание готовилось с начала весны 1918 г., но шансов на 
  то, что оно будет поддержано повсеместно, было немного. С начала лета выступление 
  казаков стало неизбежным. Нужна была последняя капля... <br>
  <br>
  В первых числах июня, прибывшая с Кавказского фронта по железной дороге 39-я 
  пехотная дивизия, занялась реквизицией зерна и скота в Александрийской, Подгорной, 
  Незлобной и Георгиевской станицах. Если быть точным, то это была не реквизиция, 
  а обычный разбой. Привыкшие к безнаказанности солдаты не церемонились ни с казаками, 
  ни с их женами. За что и поплатились - доведенные до крайности станичники взялись 
  за оружие... <br>
  <br>
  Почти в один день восстали Георгиевская, Незлобная, Подгорная, Марьинская. В 
  другом конце Пятигорского отдела поднялась Бургустанская. 18 июня зазвонили 
  колокола в Моздокском районе - выступила Луковская, казаки которой после короткого 
  кровопролитного боя взяли Моздок. В станицах начали формироваться боевые сотни. 
  <br>
  <br>
  23 июня, ставшее официальной датой начала восстания, в Моздоке собрался казачье-крестьянский 
  съезд, вынесший постановление о полном разрыве с большевиками. На съезде был 
  избран Терский казачье-крестьянский совет, который возглавил старый революционер, 
  инженер Георгий Бичерахов<sup>3</sup>. Его эсеровская программа не могла удовлетворить 
  требования казаков, желавших установить прежний порядок, но политические лозунги 
  уже никого не интересовали - Бичерахов был против большевиков и этого было достаточно. 
  Товарищем руководителя казачье-крестьянского Совета стал Григорий Вертепов<sup>4</sup>, 
  близкий друг покойного Войскового атамана М.А. Караулова<sup>5</sup>, пользовавшийся 
  огромным уважением и авторитетом среди населения Терской области. <br>
  <br>
  В этот же день на съезде назначили командующих линиями: Моздокской - полковника 
  Вдовенко<sup>6</sup>, Кизлярской - полковника Урчукина<sup>7</sup>, Сунженской 
  - полковника Долгова<sup>8</sup>, Владикавказской - полковника Данильченко<sup>9</sup>, 
  Пятигорской - полковника Владимира Агоева. Командующим войсками Терского Войска 
  стал генерал-майор Эльмурза Мистулов<sup>10</sup>, начальником штаба - полковник 
  Владимир Белогорцев<sup>11</sup>. <br>
  <br>
  Шаг от политического противостояния к вооруженной борьбе был сделан. Враждующие 
  стороны стали готовиться к войне. Первое вооруженное столкновение, случившиеся 
  в районе станицы Прохладной в последних числах июня, завершилось победой казаков. 
  Терцы воодушевились и стали готовиться к более решительным действиям в борьбе 
  с Советами. <br>
  <br>
  Центром большевизма в Терской области был Владикавказ, и не раз среди казачьего 
  офицерства звучали голоса разогнать большевиков здесь, как это уже было сделано 
  в Моздоке. Но, при всем накале противостояния, Терский казачье-крестьянский 
  совет не желал этого. В Совете преобладали левые взгляды. Здесь считали, что 
  занятие Владикавказа казаками настроит горцев против них. Г. Бичерахов неоднократно 
  заявлял, что Терский казачье-крестьянский совет воюет не с народной властью, 
  а с большевиками. Но иначе считали многие офицеры. Главным инициатором занятия 
  Владикавказа стал начальник штаба Владикавказской линии Генерального штаба полковник 
  С. Соколов<sup>12</sup>. <br>
  <br>
  Подготовка к походу на Владикавказ шла в станице Ардонской, где стали собираться 
  офицеры, недовольные политикой Советской власти. Здесь же шли переговоры казаков 
  с осетинским офицерством. Основная масса офицеров-осетин поддерживала борьбу 
  с большевиками, но в целом осетины держали нейтралитет. <br>
  <br>
  План захвата Владикавказа состоял в следующем. Полковник Соколов с отрядом из 
  архонских и ардонских казаков вступает в город и занимает Совдеп. Одновременно 
  выступают отряды самообороны Владикавказа, руководимые полковником Беликовым<sup>13</sup> 
  и осетины Осетинской слободки, во главе с полковниками Ивановым<sup>14</sup> 
  и Горшковым<sup>15</sup>. Казаки станиц Сунженской и Тарской занимают Владикавказский 
  железнодорожный вокзал. Змейские казаки перерезают железную дорогу в районе 
  Эльхотово, чтобы к Владикавказу не подошло подкрепление. Кроме этого, в поддержку 
  казаков должны были выступить осетинские отряды полковников Кибирова<sup>16</sup>, 
  Голиева и другие. Общая численность задействованных в операции людей была не 
  более 1000 человек. <br>
  <br>
  План был разработан, но его требовалось согласовать с Казачье-крестьянским советом 
  и получить от него разрешение на занятие Владикавказа. Для этой цели в Моздок 
  отправился полковник Данильченко. <br>
  <br>
  Сейчас трудно сказать, получил ли полковник Данильченко разрешение на занятие 
  Владикавказа. Разные источники противоречат друг другу. Скорее всего, Бичерахов 
  решил, что в случае успеха он поддержит полковника Соколова, а в случае неудачи 
  обвинит его в самодеятельности. Сам полковник Данильченко к началу владикавказской 
  операции не прибыл (нет ни одного упоминания об участии его в боях во Владикавказе). 
  <br>
  <br>
  Во Владикавказе в эти дни проходил 4-й съезд народов Терской области, в работе 
  которого принимал участие Чрезвычайный комиссар на Северном Кавказе Г.К. Орджоникидзе. 
  Как представитель Москвы Орджоникидзе обладал неограниченными полномочиями и 
  сыграл заметную роль в событиях на Тереке в 1918 г. <br>
  <br>
  Несмотря на то, что части Красной армии Терской республики находились на нескольких 
  фронтах, во Владикавказе Советы опирались на значительные силы. В первую очередь 
  это был 1-й Владикавказский пехотный полк (800 человек). Большая его часть охраняла 
  4-й съезд народов Терской области, который проходил в здании бывшего кадетского 
  корпуса на окраине города, остальные красноармейцы находились в Совдепе (бывший 
  штаб 21-й пехотной дивизии) и бывших казармах Апшеронского полка. В июне 1918 
  г. во Владикавказ прибыл отряд грузинских большевиков во главе с Гегечкори (300 
  человек). В городе так же находились: красноармейский отряд из китайцев (400 
  человек), отряд керменистов-осетин (200 человек), часть отряда Беленковича, 
  рабочая самооборона Курской и Молоканской слободок и артиллерийская часть. Кроме 
  этого, на вокзале стоял бронепоезд, на котором во Владикавказ прибыл Орджоникидзе. 
  <br>
  <br>
  Руководители Терской республики располагали информацией о том, что казаки планируют 
  захват Владикавказа, но само нападение застало их врасплох. <br>
  <br>
  На Преображение 24 июля (6 августа н.ст.) в 4 часа утра на рысях с 3 сотнями 
  казаков (4-я сотня находилась в резерве) прошел полковник Соколов Владимирскую 
  слободку, сразу же перешел на деревянный мост и устремился к Совдепу. Захватив 
  в 1-м реальном училище нижний этаж, и забрав там винтовки, казаки вывели во 
  двор Апшеронского собрания сонных красноармейцев. Те, не долго думая, юркнули 
  в здание 2-го реального, которое тоже было занято красными. Проснулись и жильцы 
  верхнего этажа, огляделись и быстро сориентировались: привязали на веревках 
  ручные гранаты и начали забрасывать ими нижний этаж (в окна), другие сверлили 
  пол и в образовавшиеся отверстия бросали бомбы. На улице показались небольшие 
  группы красных и открыли огонь по отдельным казакам; группы увеличивались и 
  стали кольцом сжимать 1-е реальное училище. Несколько китайцев- красноармейцев 
  взобрались на соседнюю с училищем колокольню и начали обстреливать окрестности 
  из пулемета. <br>
  <br>
  У деревянного моста сотни полковника Соколова были встречен полковником Литвиновым<sup>17</sup>, 
  который собрал отряд из офицеров и добровольцев-неказаков. Вмесе с казаками 
  отряд Литвинова принял участие в атаках на Совдеп и в последующих боях в городе<sup>18</sup>. 
  <br>
  <br>
  В первый день операции площадь перед зданием Совдепа стала ареной ожесточенных 
  боев. По свидетельству очевидцев, вся площадь была покрыта телами погибших казаков 
  и красноармейцев. В плен к казакам попало несколько десятков красноармейцев 
  (в том числе 50 китайцев). <br>
  <br>
  Здание Совдепа было захвачено 3-й сотней (около 50 казаков) во главе с хорунжим 
  Сагайдаковым<sup>19</sup>. Оказавшись в окружении и не получив поддержки, когда 
  кончились патроны, хорунжий Сагайдаков и 17 оставшихся в живых кзаков в 11 часов 
  утра сдались<sup>20</sup>. <br>
  <br>
  Когда полковник Соколов перешел деревянный мост, часть казаков пошла на Совдеп, 
  другая - в центр города, а мост остался без охраны. Он был занят красноармейцами, 
  и 4-я сотня войскового старшины Савченко<sup>21</sup> не смогла его пройти. 
  Среди казаков, оставшихся в центре города, началась паника. Тем временем, в 
  поддержку казаков выступила осетинская самооборона во главе с полковниками Ивановым 
  и Горшковым, а также сотня осетин-гизельцев во главе с полковником Голиевым. 
  В течение всего первого дня операции на поддержку полковнику Соколову подходили 
  небольшие отряды из казачьих станиц (Архонской, Сунженской, Тарской, Ардонской, 
  Николаевской, Змейской) и осетинких селений (Гизели, Хумалага, Владимирского, 
  Ардона). К концу дня восставшие контролировали почти всю левобережную часть 
  города и центр. В руках большевиков осталась южная окраина Владикавказа (Молоканская 
  слободка с кадетским корпусом) и северная (вокзал, военный госпиталь, Coвдеп 
  и Курская слободка). В южной, части обороной города руководил Совнарком, а северной 
  — Г.К. Орджоникидзе, заведующий военным отделом Совдепа С. Мартынов и руководитель 
  самообороны Курской слободки П. Огурцов. В первые дни связи между южным и северным 
  очагами обороны не было. <br>
  <br>
  Первоначально успех был на стороне восставших. Казаками было арестовано и расстреляно 
  несколько десятков большевиков. Советские историки говорят о более чем 100 человек, 
  расстрелянных казаками, и называют стандартный список погибших: Н. Кесаев, Ф. 
  Камалов, И. Никитин, Ф. Серобабов, С. Шмулевич, Огнев, Чхубиани. Имена других 
  не упоминаются, и трудно сказать, кто они. Зато известно, что целый ряд видных 
  большевиков (некоторые из которых были в черном списке полковника Соколова как 
  подлежащие расстрелу в первую очередь), попав в плен, избежали расстрела. Это 
  были члены Терского совнаркома Ю. Фигатнер, А. Андреев, К. Дигуров, во главе 
  с самим председателем Терского Совнаркома Ю. Пашковским и командиром грузинского 
  отряда А. Гегечкори и и другими. Есть несколько версий о том, почему они были 
  отпущены. Некоторые источники пишут, что они были арестованы не казаками, а 
  осетинами из отряда полковника Иванова. Иванов и Дигуров вместе выросли, поэтому 
  Иванов отпустил Дигурова и остальных. Другие считают, что эти арестованные были 
  членами Съезда народов Терской области, и их опустили по просьбе Съезда. Что 
  произошло на самом деле, неясно, но наиболее вероятна вторая версия, так как 
  левые взгляды преобладали среди гражданских руководителей восстания. <br>
  <br>
  27 июля (9 августа) атака казаков и осетин на правобережную часть пошла энергичней; 
  красноармейцы эвакуировали магазин Зингера и Совдеп. Восставшие с боем продвинулись 
  до улицы Льва Толстого, перейдя деревянный мост под пулеметным огнем противника. 
  Продвигаться к Госпитальной улице без соответствующей артиллерийской подготовки 
  было невозможно, так как на ней были хорошо приспособлены к обороне дом умалишенных, 
  старая тюрьма, госпитальная ограда и городская больница. <br>
  <br>
  На следующий день боевые действия были не столь энергичны. Имело место лишь 
  продвижение отставшего правого участка полковника Рощупкина и частичное выдвижение 
  фронта на Госпитальной улице. На Молоканском фронте казаки заняли сильно охватывающее 
  (в «клещи») положение. Там все время готовились к атаке, но почему-то ни верхне-осетинцы, 
  ни гизельцы в решительную атаку не перешли. Тем временем, молокане заговорили 
  о мире, что сказалось на настроении Молоканского фронта, так как заявление их 
  было, безусловно, неискренним, а, между тем, им поверили, главным образом, в 
  штабе полковника Соколова. <br>
  <br>
  На четвертый день боев в городе, Орджоникидзе решил, что город удержать невозможно 
  и отдал приказ об оставлении Владикавказа. Его бронепоезд и красноармейцы, оборонявшие 
  вокзал, ушли к Беслану. Напоследок бронепоезд выпустил по городу все оставшиеся 
  снаряды. Г.К. Орджоникидзе позднее вспоминал: «Бронированный поезд под командованием 
  Автономова в продолжение четырех дней громил город из орудий. А когда было решено 
  уходить, был дан приказ выпустить все оставшиеся снаряды. И бронепоезд дал по 
  городу за ночь 200 выстрелов»<sup>22</sup>. Можно представить, что творилось 
  на улицах Владикавказа после этих артобстрелов. Ведь стреляли не по целям, а 
  просто по городу. К этому следует добавить, что город также обстреливался гаубицами, 
  стоявшими у кадетского корпуса. <br>
  <br>
  Покидая город, Орджоникидзе распорядился расстрелять всех арестованных. Во время 
  сражений в плен к красноармейцам попали десятки казаков, осетин, офицеров и 
  добровольцев. Их всех отводили в комендатуру. Сюда же собирали арестованных 
  по подозрению в мятеже или просто как подозрительных. Красноармеец Р. Битимиров 
  вспоминал, как они в поисках мятежников в одной из квартир нашли карабин и именную 
  шашку. Шашка принадлежала хозяину квартиры генералу П. Хелмицкому<sup>23</sup>, 
  в квартире также находились три полковника - сын генерала и зятья. Никаких улик 
  их участия в мятеже найдено не было (карабин был в масле). Красноармейцы не 
  знали, что делать с задержанными. Затем по распоряжению коменданта Мартынова 
  их отвели в комендатуру «на всякий случай», допросили и посадили в камеру. За 
  это «геройское дело» Мартынов подарил Битимирову карабин генерала, а его товарищу 
  Ахсарову шашку генерала. «8 августа, - воспоминает Битимиров, - Мартынов приказал 
  всех вести к вокзалу и все время торопил нас. Генерал Хелмицкий заявил, что 
  он болен, идти не может, и просил отпустить его домой. Но ему отказали и понесли 
  его на руках. Около вокзала колонна была остановлена и Мартынов зашел в бронепоезд. 
  Не прошло и пяти минут, как он вышел и распорядился всех пленных повести дальше 
  на север по Вокзальной (ныне Маркова) улице к Базоркинской дороге (ныне Черменское 
  шоссе)». В дальнейшем всех пленных расстреляли за Госпитальным кладбищем, якобы 
  за попытки к бегству. Что интересно, расстреливать помог отряд китайцев, который 
  встретил колонну пленных на выходе из города. И, еще не зная, что пленные будут 
  «бежать», пошел с красноармейцами «за компанию»<sup>24</sup>. Сколько было арестованных 
  в этой колонне, неизвестно. Есть лишь свидетельство о 20 плененных казаках. 
  А сколько было арестованных в качестве заложников?! Таких, как генерал Хелмицкий. 
  Известно, что среди расстрелянных заложников был есаул Алексей Сериков<sup>25</sup>, 
  кавалер ордена Св. Георгия 4-й степени. Он также был арестован только за то, 
  что являлся офицером. В числе жертв красного террора был и всеми уважаемый в 
  городе директор Владикавказской мужской гимназии Иосиф Бигаев. <br>
  <br>
  Несмотря на успехи, восставшие не смогли до конца скоординировать свои действия. 
  Постоянно шли разногласия между казачьими офицерами и офицерами-неказаками по 
  вопросу, кто кому подчиняется. Каждый отряд воевал самостоятельно, порой не 
  зная, что делается на соседнем участке. Самостоятельно действовали и осетинские 
  отряды. <br>
  <br>
  Организуя поход на Владикавказ, Данильченко и Соколов сформировали конные и 
  пешие сотни из ардонских и архонских казаков. Они были объединены в 1-й Владикавказский 
  конный полк во главе с войсковым старшиной Николаем Савченко и в 1-й Владикавказский 
  пеший батальон, который возглавил полковник Павел Золотарев<sup>26</sup>. Казаков 
  станиц Сунженской, Тарской, Фельдмаршальской и Воронцово-Дашковской повел на 
  Владикавказ полковник Николай Рощупкин<sup>27</sup>, казачьей артиллерией командовал 
  есаул Владимир Антонов<sup>28</sup>. Все названные лица руководили казаками, 
  но в боях в городе участвовали и другие формирования. <br>
  <br>
  Отрядами самообороны центральной части города руководил полковник Иван Беликов. 
  Недолюбливая казаков, он оставил воспоминания, в которых в неудаче Владикавказской 
  операции обвинил последних.<sup>29</sup><br>
  <br>
  В сражениях на улицах города активно участвовал отряд полковника Бориса Литвинова. 
  Этот отряд состоял из офицеров и добровольцев-неказаков. В своих воспоминаниях 
  Беликов Литвинова вовсе не упоминает, сильно преувеличивая собственную роль 
  в организации восстания. <br>
  <br>
  Довольно подробно описывая то, как он поднимал владикавказских осетин на борьбу 
  с Советами, Беликов не упоминает и полковников Горшкова и Иванова. А ведь именно 
  они руководили осетинскими отрядами в городе. Нельзя думать, что Беликов не 
  был знаком с ними. Полковника Михаила Иванова (осетина по национальности) знал 
  весь город. Некоторое время он был владикавказским полицмейстером, затем его 

  перевели на ту же должность в Ростов-на-Дону. А в 1916 г. Иванова назначили 
  командиром батальона Осетинской пешей бригады, с которой он и вернулся во Владикавказ. 
  <br>
  <br>
  Еще лучше Беликов знал полковника Константина Горшкова. Они служили в одном 
  полку - 81-м пехотном Апшеронском. В 1914 г., уходя из Владикавказа на фронт, 
  Горшков был капитаном, а Беликов штабс-капитаном. В феврале 1918г., когда Беликов 
  был начальником гарнизона Владикавказа, полковник Горшков был назначен начальником 
  Осетинского района, сменив полковника Колиева. <br>
  <br>
  Полковники Иванов и Горшков весьма деятельно занимались организацией самообороны, 
  а затем подготовкой вооруженного выступления на Осетинской слободке. По распоряжению 
  полковника Иванова на Верхне-Осетинской слободке были возведены различные фортификационные 
  сооружения. На свои деньги (более 12 000 рублей) он купил для осетинской самообороны 
  оружие и боеприпасы. Обо всем этом Беликов умалчивает. <br>
  <br>
  Беликов постоянно конфликтовал со штабом полковника Соколова и его ближайшим 
  окружением: полковником Лавровым<sup>30</sup>, войсковым старшиной Морозовым<sup>31</sup>. 
  Те в свою очередь не всегда находили общий язык с командующим Молоканским фронтом 
  полковником Голиевым и командиром осетин в Верхне-Осетинской слободке полковником 
  Зембатовым. <br>
  <br>
  До конца Владикавказской операции так и не были окончательно улажены все разногласия 
  между боевыми отрядами, что, конечно, сказалось на успехе всей операции. Более 
  согласованно решались вопросы гражданского управления. После вступления казаков 
  в город из членов Терского народного совета и делегатов съезда Терской области 
  был сформирован временный Исполнительный Комитет во главе с эсером И. Семеновым 
  и Народный Совет, который возглавил эсер В. Полюхов. В Народный Совет вошли 
  17 казаков, эсеров, меньшевиков и 7 чеченцев во главе с Я. Арсановым (сыном 
  убитого ранее шейха Чечни Д. Арсанова). Были сформированы городские органы власти: 
  городская управа во главе с И. Цирульниковым и городская милиция с начальником 
  Подаговым. Новые власти старались действовать законно. Так, на протяжении всего 
  времени пока казаки находились в городе, нетронутыми оставались Госбанк, казначейство 
  и некоторые другие учреждения. При аресте наркома финансов Фигатнера была изъята 
  большая сумма денег. Впоследствии бывшие члены временного Исполкома об этой 
  сумме неоднократно отчитывались. <br>
  <br>
  2 (15) августа во Владикавказ прибыл Г. Бичерахов. Выступив с докладом на заседании 
  временного Исполкома, он в очередной раз заявил, что лично был против нападения 
  на Владикавказ и что Казачье-Крестьянский Совет воюет лишь с большевиками, а 
  не с народами Терской области. Затем председатель временного Исполкома И. Семенов 
  кратко рассказал о задачах новой власти: «Областной Народный совет выделил временный 
  исполнительный комитет, задачи которого немедленное водворение мира и создание 
  порядка в области, необходимого для созыва 5-го Народного областного съезда, 
  который и выделит постоянную народную власть и будет полным хозяином, чтобы 
  судить все наши поступки. Мы не знаем, удастся ли нам довести дело до конца, 
  но мы примем все меры, чтобы выполнить возложенную на нас задачу. Мы не будем 
  входить в оценку действий казачье-крестьянского совета, ибо, повторяю, это дело 
  5-го областного Народного съезда. Сейчас нужно найти все средства, чтобы выйти 
  из создавшегося положения, дабы спасти дело всей демократии в области»<sup>32</sup>.<br>
  <br>
  Бичерахов прибыл вместе с новым командующим всеми боевыми силами во Владикавказе 
  генералом Мадритовым. Эта кандидатура не устроила казаков, так как для них Мадритов 
  был чужаком, А между тем положение противоборствующих сил начало кардинально 
  меняться. Боеприпасов казакам по-прежнему не хватало, у единственного орудия 
  есаула Антонова было лишь три снаряда, броневик сотника Григорьева<sup>33</sup> 
  сломался и попал в руки красных, казаки и добровольцы заметно устали. Отряды 
  осетин стали больше грабить, чем воевать. Этот факт отмечен во многих источниках<sup>34</sup>. 
  Но, кроме этого, на стороне большевиков выступили ингуши. <br>
  <br>
  Как было сказано выше, Г.К. Оpджоникидзе отбыл в Беслан, оттуда направился в 
  Ингушетию и призвал ингушей поддержать советы, за что они могли получить земли 
  казачьих станиц по Сунже. Ингушам такие слова пришлись по нраву и четыре сотни 
  ингушских всадников под командованием бывшего полковнике Муссы Саутиева и полного 
  георгиевского кавалера Хизира Орцханова двинулись на Владикавказ. Сотни Саутиева 
  и Орцханова, наступавшие на вокзал и слободку Шалдон, были отбиты, так как ингуши 
  были плохо обучены уличным боям. Но еще более значительные сиво ингушей стали 
  окружать станицы Сунженскую, Тарскую, Воронцово-Дашковскую и Фельдмаршальскую. 
  Последний факт стал решающем в падении Владикавказа. Казаки стали покидать город 
  и отправляться на защиту родных станиц. Видя, что казаки уходят, упал боевой 
  дух отрядов самообороны и Добровольческого отряда. <br>
  <br>
  На помощь красным из Грозного прибыл отряд рабочих (300 человек) под командованием 
  бывшего офицера Генштаба М. Левандовского. Стали подходить отряды керменистов 
  (осетинских большевиков) из селений Заманкул (400 человек) и Зальги (400 человек). 
  Вновь вернулся бронепоезд Г.K. Орджоникидзе, который с боем занял владикавказский 
  вокзал. Кроме этого, еще один бронепоезд стал обстреливать станицу Архонскую, 
  создавая впечатление, что на нее готовится наступление. Это вынудило и архонцев 
  уходить из города. Ситуация стала критической, удержать город возможности не 
  было. <br>
  <br>
  4 (17) августа, ночью, все участники Владикавказской операции во главе с временным 
  Исполкомом покинули город. Так печально закончились одиннадцатидневные бои во 
  Владикавказе, получившие позднее название Августовских событий. Главная масса 
  ушедших с оружием в руках казаков, офицеров и добровольцев отошла в станицу 
  Архонскую, недалеко от Владикавказа. <br>
  <br>
  Сейчас трудно судить об общем числе погибших. Известно лишь, что архонские казаки 
  потеряли убитыми 100 человек. Погибло несколько десятков казаков из станицы 
  Ардонской. Было убито 22 осетина (в т.ч. поручик Алексей Дзампаев, подпоручик 
  Александр Дзахсоров, прапорщик Цалоев). Из погибших также следует отметить полковника 
  Александра Иванова, подъесаула Вячеслава Изюмского, надворного советника Ивана 
  Бычкова. Немало было жертв и среди мирных жителей города. Красноармейцы стреляли 
  из гаубиц с одной улицы на другую. Чтобы выбить противника из домов последние 
  поджигали. Таким образом, выгорели целые улицы (остовы некоторых домов стояли 
  до середины 1930-х гг.). <br>
  <br>
  Среди жертв были иностранные Граждане. 15 августа одной из шальных (а может 
  и нет) пуль был убит глава английской миссии полковник Д. Пайк<sup>35</sup>. 
  <br>
  <br>
  Но главная трагедия для жителей Владикавказа началась, когда казаки и добровольцы 
  покинули город. Начались поиски и аресты всех подозреваемых в мятеже. Председатель 
  Терской ЧК К. Цинцадзе писал: «Было расстреляно несколько десятков главарей 
  контрреволюционного восстания»<sup>36</sup>. Кто был среди этих «десятков главарей», 
  сказать трудно, потому что все, кто был замешан в восстании, ушли из Владикавказа, 
  остались лишь те, кто не чувствовал за собой вины. К. Цинцадзе сам говорит об 
  этом: «Следует отметить, что почти все меньшевики и эсеры из состава Терского 
  правительства убежали вместе с контрреволюционерами». Это естественно, видные 
  контрреволюционеры, конечно же, не ждали, пока их арестуют. Все организаторы 
  мятежа во Владикавказе остались живы и в дальнейшем участвовали в Белом движении. 
  Это полковники Иван Беликов, Степан Соколов, Николай Рошупкин, Алексей Данильченко, 
  Борис Литвинов, Михаил Иванов, Константин Горшков, Павел Золотарев и другие. 
  Владикавказ покинули все, кто опасался мести большевиков. После городских боев 
  опустели целые улицы, жители которых покинули Владикавказ. Так на улице Тифлиссской 
  опустели 28 домов, на Владимирской — 27, на Ардонской -12...<sup>37</sup><br>
  <br>
  Расстрелы за Госпитальным кладбищем, которые начались во время Августовских 
  событий, продолжались. В дневнике «Полгода во Владикавказе» анонимный автор 
  писал: «16 сентября - ...в кукурузе валяются трупы убитых людей, еле присыпанных 
  землей так, что видны конечности. 18 ноября - Сегодня, говорят, расстреляно 
  13 человек-контрреволюционеров. На завтра приговорено к смерти еще 60. 19 ноября 
  - трупы 16 расстрелянных валялись за Алагирским заводом (ныне «Электроцинк» 
  — Ф.К.). 2 декабря - расстреляно не 13 казачьих офицеров, а 167.»<sup>38</sup><br>
  <br>
  При приближении частей Добровольческой армии к Владикавказу расстрелы усилились. 
  А когда белые заняли город, начались массовые похороны. В церковных книгах читаем: 
  дата смерти - 24 июля, 1, 2, 4, 5, 6 августа, а дата погребения - 16 февраля, 
  24 февраля, 1 марта, 18 апреля 1919 г. Такова ситуация и по городу, и по станице 
  Архонской. Это еще одна из загадок Августовских событий. Если арестованные были 
  расстреляны еще в августе 1918 г., то почему их тела родственники стали спешно 
  хоронить в феврале 1919 г., когда белые уже почти заняли город. А если они были 
  расстреляны только в начале 1919 г. (что объясняет торопливость в похоронах), 
  то почему в датах смерти указаны дни Августовских событий. Во всем этом еще 
  предстоит разобраться. <br>
  <br>
  Августовские события были большой трагедией, но один из организаторов Терского 
  восстания, полковник Николай Букановский<sup>39</sup> так сказал: «Я считаю, 
  что владикавказские события принесли много горя. Они, может быть, на 80 процентов 
  ухудшили дело, но я ни минуты не сомневаюсь, что полковник Соколов, полковник 
  Данильченко поступили так, как должны были поступить»<sup>40</sup>. Сейчас мы 
  понимаем, что он был прав. Необходимо было бороться за свои права. <br>
  <br>
  Говоря о причинах неудачи Владикавказской операции, можно отметить, в первую 
  очередь, слабую организованность и плохую координацию всех задействованных в 
  операции сил, отсутствие единоначалия и конкретного плана действий после занятия 
  Владикавказа у восставших. Наконец, восставшие не были едины, поскольку среди 
  них были, с одной стороны, сторонники левых взглядов (Бичерахов, Семенов, Полюхов 
  и другие), а с другой — казачье офицерство, желавшее соединиться с Добровольческой 
  армией. <br>
  <br>
  Совершенно верно причины неудачи Владикавказской операции отметил один из участников 
  Терского восстания 1918 г. Г. Горбач:<sup>41</sup> «С первого же дня стало ясным, 
  что операция принимает затяжной характер и это - благодаря причинам, приведшим 
  в конечном итоге и к полной нашей неудаче. Осетины оказались слабыми союзниками, 
  ибо не считались ни с какими распоряжениями начальников, не проявляли большого 
  порыва, а по ночам даже уходили с позиций. Казаки тоже не сознавали как будто 
  всей важности предпринятой операции. Энергия их падала с каждым днем и, как 
  только распространился слух (оказавшийся верным) о нападении ингушей на станицы 
  Тарскую и Сунженскую, потянулись защищать свои дома даже и те, на чьи станицы 
  никто и не нападал, бросая по недомыслию своему так блестяще начатое дело. Вся 
  тяжесть борьбы легла на плечи добровольцев, главным образом офицеров, вышедших 
  с оружием в руках при первых же выстрелах. С первых же дней боев они выполняли 
  задачи, которые никому нельзя было поручить, неизменно проявляя выдающиеся мужество 
  и самоотверженность»<sup>42</sup>. <br>
  <br>
  На наш взгляд, Владикавказская операция и Терское восстание в целом продемонстрировали 
  недальновидность и пассивность основной массы казачества, а также мужество и 
  героизм тех, кто не побоялся выступить против попрания своих прав. <br>
