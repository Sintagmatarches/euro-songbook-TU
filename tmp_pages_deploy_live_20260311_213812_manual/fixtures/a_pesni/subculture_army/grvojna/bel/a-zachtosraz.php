	<span class="text"><strong>И.М. Ходаков </strong><br>
  <br>
       <span class="header"> <strong>ЗА ЧТО СРАЖАЛИСЬ ПОТОМКИ СТРАНСТВУЮЩИХ 
        РЫЦАРЕЙ ЧЕРНОГО НАРОДА И СТЕПНЫХ РАЗБОЙНИКОВ<sup>1</sup>. РАЗМЫШЛЕНИЯ 
        БЕЛОГВАРДЕЙЦЕВ ОБ ОСНОВАХ МОРАЛЬНО-ПСИХОЛОГИЧЕСКОГО И БОЕВОГО ДУХА КАЗАКОВ 
        ВО ВРЕМЯ ГРАЖДАНСКОЙ ВОЙНЫ <br>
        </strong></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 10-16</em>.<br>
  <br>
  <br>
  <em>«Моральные величины на войне занимают самое важное место, Эти моральные 
  величины насквозь пропитывают всю военную стихию...»</em><sup>2</sup><em><br>
  К. фон Клаузевиц </em><br>
  <br>
  О казачестве, его прошлом и настоящем пишут немало, эта тема действительно актуальна 
  (актуальна хотя бы потому, что исконные земли донских, кубанских и, в первую 
  очередь, терских казаков находятся в непосредственной близости от Северного 
  Кавказа, который вот уже не первое столетие является горячей точкой России). 
  Но что же представляет собой казачество с точки зрения ментальных установок? 
  <br>
  <br>
  Как единое целое казачество перестало существовать после Гражданской войны, 
  когда огромное количество его представителей ушло в Зарубежье, сохраняя, при 
  этом, даже в дали от Родины неповторимое своеобразие быта. Миропонимание же 
  казаков, появившихся на свет и воспитанных в СССР, весьма отличается от миропонимания 
  их предков. Не стоит забывать, что казачество — прежде всего военное сословие, 
  и именно как военное сословие оно пытается сегодня возродиться. В этой связи 
  естественным образом возникает вопрос о боевом духе и морально-психологическом 
  состоянии казаков в годы Гражданской войны, ибо любая война раскрывает характер 
  народа, его дух, нравственный потенциал и жизнеспособность. <br>
  <br>
  Не претендуя на бесспорность выводов, автор пытается обозначить данную проблему, 
  считая необходимым обратить внимание читателя на очень важные детали, без понимания 
  которых сложно исследовать менталитет казаков: в годы Гражданской войны впервые 
  в истории было создано независимое, возглавленное генералом Красновым, донское 
  казачье государство, организованы регулярные казачьи вооруженные силы, призванные 
  самостоятельно решать задачи государственного масштаба — позже то же самое, 
  но с еще меньшим успехом, попытались сделать на Кубани. <br>
  <br>
  Наконец, обращаясь при исследовании морали и боевого духа казаков к воспоминаниям, 
  важно учитывать, что этот вид исторического источника носит в значительной мере 
  субъективный характер. <br>
  <br>
  Что же лежит в основе боевого духа всей армии и морально-боевых качеств отдельного 
  воина? По словам Клаузевица, к основным моральным потенциям армии относятся: 
  1) таланты полководца; 2) воинская доблесть армии; 3) дух комплектующего ее 
  народа<sup>3</sup>. Под духом комплектующего армию народа немецкий военный мыслитель 
  подразумевал энтузиазм, веру, фанатизм, убеждения<sup>4</sup>. Итак, на чем 
  же был основан энтузиазм казаков во время Гражданской войны? Во что они верили 
  и на кого надеялись, поднимая оружие против большевиков? В чем причина их доблести 
  или отсутствия таковой? <br>
  <br>
  Мы остановим внимание на воспоминаниях полковника Кубанского казачьего войска 
  Ф.И. Елисеева, командующего Кубанской армией генерал-лейтенанта А.Г. Шкуро, 
  атамана Всевеликого войска Донского генерала от кавалерии П.Н. Краснова и главкома 
  Вооруженных Сил на Юге России генерал-лейтенанта А.И. Деникина. Почему к мемуарам 
  именно этих авторов я решил обратиться? Не в последнюю очередь потому, что перечисленные 
  офицеры были не только храбрыми, беззаветно любившими Родину людьми, доказавшими 
  свою преданность России на кровавых полях Первой мировой, но и незаурядными 
  писателями (исключая, разумеется, Шкуро, который свои мемуары диктовал). <br>
  <br>
  «Как «главный» теперь за семейным столом и «герой войны» — встал я с бокалом 
  в руке. Встали также все — и урядники и семья. Попросил их сесть, так как говорить 
  седячим - легче, видя их всех. Все сели. Молчание. Все сосредоточились. <br>
  <br>
  — Братцы... — тихо, спокойно начал я. — Война окончена... мы дома... <br>
  <br>
  Что случилось со мной, — не знаю и теперь, но «что-то» резко схватило меня за 
  горло... остановился язык... кровь бросилась в лицо и я разрыдался как дитя, 
  упав головою на стол. А за мною зарыдали все, в особенности женщины. Но отец!.. 
  Такая умница и гордость! Пышная борода и густые усы. При других — одно горделивое 
  достоинство! Отец трех сыновей-офицеров! И он разрыдался...»<sup>5</sup>. Так 
  описывает свое возвращение домой с Первой мировой войны Федор Иванович Елисеев 
  — автор ярких, талантливо написанных и обширных воспоминаний о Гражданской войне. 
  Тогда, за праздничным столом, молодой и храбрый подъесаул 1-го Кавказского полка 
  Кубанского казачьего войска не совладал с нахлынувшими чувствами... наверное, 
  те слезы были слезами радости по поводу законченной, наконец- то, войны, хотя, 
  вспоминая о завершении обеда, Елисеев писал: «Со мною они (однополчане — И.Х.) 
  прощались как братья, с которыми я окончил что-то большое и страшное и теперь 
  идущее в неизвестность...»<sup>6</sup>. Страшное было еще не окончено... В тот 
  вечер молодой подъесаул, вернувшийся из Финляндии, где был расположен его полк, 
  скорее всего, догадывался: началась война более ужасная, нежели с внешним врагом. 
  <br>
  <br>
  Федору Ивановичу Елисееву выпало испить горькую чашу Гражданской войны до дна 
  — он дрался с красными на Кубани и Маныче, принимал участие в боях под Воронежем 
  и разделил с родными кубанцами унижение капитуляции и плена, в 1921 г. Елисеев 
  бежал из большевицкой России, с группой казаков-джигитов объехал весь мир, война 
  снова настигла его в 1939 г. на Суматре. Полковник Елисеев вступил в Иностранный 
  легион и сражался с японцами. В 1945 г., «прикрывая отход батальона легионеров 
  в арьергарде и спасая раненого товарища, сам дважды раненый, попадает в плен»<sup>7</sup>. 
  Спустя год Елисеев был освобожден и вернулся во Францию, позже переехал в Америку, 
  где и умер в 1987 г. в возрасте 94 лет. Испытания, выпавшие на долю Елисеева, 
  не ожесточили его сердце, на страницах воспоминаний он не стремится облить противника 
  грязью, напротив, его цель — разобраться, понять, за что воевали красные, почему 
  они победили. Проведя всю Первую мировую и Гражданскую войны в рядах казачьих 
  войск, называя самого себя казакоманом, Елисеев, несомненно, прекрасно изучил 
  морально-психологические особенности представителей сословия и мог объективно 
  оценить боевой дух казаков. <br>
  <br>
  Во многом, психологию казаков объясняет следующий эпизод. Вернувшиеся с войны 
  кубанцы приглашены подъесаулом Елисеевым к нему в дом: «Десятка полтора урядников, 
  привязав под сараями своих лошадей... с полным вооружением, с винтовками в руках, 
  снимая папахи и крестясь на иконы, вошли в дом»<sup>8</sup>. Подобная сцена 
  указывает на <em>патриархальность</em> (здесь и далее все выделения в тексте 
  сделаны мной — И.Х.) сознания казаков. Воспитанные в <em>традиционном</em>, 
  в данном случае, <em>православном</em>, обществе, кубанцы (донцы, терцы) имели 
  четкие представление о добре и зле, выражавшиеся в том, что те, кто за Бога, 
  — на стороне добра, кто же является противником Господа — на стороне зла, такое 
  миропонимание, во многом, объясняет жестокое отношение казаков к пленным красноармейцам, 
  особенно комиссарам, ибо те, кто разрушает церкви, уничтожает святые иконы и 
  убивает священников, в представлении казаков и не люди вовсе, следовательно, 
  к ним нет необходимости испытывать чувство жалости. В своих воспоминаниях Елисеев 
  приводит слова одной кубанской песни, с которой восставшие весной 1918 г. казаки 
  шли сражаться с большевиками: <br>
  <br>
  «С Богом, куба-а-нцы, не ро-бея-а, <br>
  Сме-е-ело в бой — пойдем, дру-зья-а-а...— <br>
  Бейте, режьте — не жалея, <br>
  Ба-а-асур-ма-а-нина врага!»<sup>9</sup><br>
  <br>
  Необходимо помнить, что в казачьей среде было <em>много старообрядцев</em>, 
  ревностно хранивших все внешние формы традиции. Старообрядцы приняли самое активное 
  участие в Белом движении (например, Гундоровский отряд Донской армии целиком 
  состоял из старообрядцев), хотя сражались, скорее за веру, т.е. в их понимании 
  война носила <em>религиозный</em> характер. <br>
  <br>
  Одними из первых сопротивление новой, большевистской, власти оказали <em>казаки-старики</em>. 
  Тогда, в первые месяцы 1918 г. «И на местах по станицам, и в центре отдела — 
  власть (казачья — И.Х.) была бессильна, чтобы остановить революционно-разрушительную 
  стихию. И лишь родные страдальцы старики, эти стойкие хранители вековой <em>казачьей 
  патриархальности и казачьей обыкновенности</em>, да все поголовно бабы-казачки 
  — они с нескрываемой злобою выступали против «хрантавиков», тыча им в глаза 
  проклятия»<sup>10</sup>. Почему же фронтовики стали объектом проклятий стариков-казаков 
  и казачек? «Фронтовики, безусловно, устали и хотели окончания своей военной 
  службы, что было вполне нормально»<sup>11</sup>. Уставшие, не раз смотревшие 
  смерти в лицо, фронтовики не спешили оказывать сопротивление большевизации Кубани, 
  чем и заслужили проклятия стариков, хранивших, как указывал Елисеев, патриархальные 
  традиции, видевших — большевики не чтут Бога, презирая все то, что веками составляло 
  сословную гордость казаков (например, их полковые знамена), заставляя их разоружаться 
  — оружие же было неотъемлемой частью жизненного уклада казака. Когда весной 
  1918 г. в одном из арсеналов крепости станицы Кавказской собравшиеся казаки 
  приняли решение выступить против Советской власти, отказавшись сдать большевикам 
  оружие, первыми, кто поддержал восставших, были старики: «И среди бушующей порывистой 
  казачьей молодежи — послышался резкий всхлипывающий плач стариков: «Деточки!.. 
  Сыны-ы!.. Поддержите-е... давно бы та-ак!»<sup>12</sup>. Именно старики не только 
  поддержали, но и первыми выступили против новой безбожной власти. Приведя выше 
  текст кубанской песни, в которой содержался призыв не испытывать жалости к басурманам, 
  Елисеев, возглавивший восстание в станице Кавказской, писал: «То была любимая 
  песня казаков, идущих в поход. Оглянулся и вижу: вся передняя шеренга песенников 
  состоит сплошь из <em>стариков-бородачей</em>, всегдашних главарей станичного 
  сбора — столпов станичного самоуправления»<sup>13</sup>. Была и еще одна, не 
  связанная с верой, причина, по которой казаки презирали красных, не испытывая 
  к ним пощады. Когда восставшие захватили станицу Темижбекскую, Елисеев призвал 
  ее жителей присоединиться к их отряду: «Сказал коротко, сжато, но зато мои <em>старики</em>, 
  в особенности Стуколов — умный, активный, ненавидевший все мужичье... «распространился 
  обо всем», чем щедро подлил масло в казачий огонь»<sup>14</sup>. <em>Ненавидевший 
  все мужичье...</em> Этой фразы достаточно, чтобы понять еще одну причину ожесточенной 
  борьбы, развернувшейся на просторах Кубани, между Таманской, состоявшей «исключительно 
  из иногородних»<sup>15</sup>, (т.е. из ненавидимого Стуколовым, мужичья) Красной 
  армией, с одной стороны и казаками с другой. «Все было у иногородних полная 
  противоположность казакам, хотя и жили они не только в одной станице, но и бок 
  о бок — соседи дворами. Редкий случай бывал, чтобы казак женился на девушке-иногородней. 
  Но чтобы девушка-казачка вышла замуж за иногороднего — это считалось просто 
  позором. <br>
  <br>
  Вот при таких психологических взаимоотношениях — неожиданно свалилась русская 
  революция 1917 г. В казачьих частях ее совершенно не ждали, а в станицах ее 
  посчитали как настоящий «бунт против царя» (посчитали именно старики с их патриархальным 
  и, несомненно, монархическим сознанием, ибо патриархальный быт, как и самодержавная 
  форма правления, подразумевают строгую иерархичность общественного (и семейного) 
  устройства. Молодые и среднего возраста казаки находились в действующей армии 
  — И.Х.). Иногородние же решили, что пришел долгожданный час, когда настало полное 
  равенство с казаками, и что теперь «казачья земля» будет даваться им, как равным 
  гражданам России»<sup>16</sup>. <br>
  <br>
  Сословная гордость казаков, наряду с патриархальным миропониманием, определяла 
  их морально-психологическое состояние, влияя на боевой дух людей, — многие из 
  них шли сражаться за поруганную веру и землю, а также мстить иногородним за 
  униженную сословную гордость. Однако все это было свойственно, прежде всего, 
  старикам и офицерам-казакам. <br>
  <br>
  За свои станицы казаки сражались доблестно и храбро (правда, после того, когда 
  убедились в жестокости Советской власти по отношении к ним), однако, выйдя за 
  пределы родной земли, казаки дрались иначе, их морально-психологическое состояние 
  и боевой дух под Воронежем, на Маныче и Волге, все же, несколько иные, нежели 
  в родных степях. Почему? Елисеев вспоминает, как однажды в феврале 1919 г. батальон 
  пластунов отказался форсировать Маныч, т.е. выходить за пределы Кубанского войска. 
  Объясняя причины этого шага, автор истории Корниловского конного полка пишет: 
  «...здесь по Манычу, в крестьянской Ставропольской губернии, воевали против 
  красных семь кубанских конных полков с артиллерией, четыре батальона кубанских 
  пластунов и... ни одного русского солдата-крестьянина своей же Ставропольской 
  губернии. Надо точно сказать, что громаднейшее большинство ставропольских крестьян-земледельцев, 
  будь то и богатые — сочувствовали советской красной армии. И казаки, живя в 
  крестьянских хатах, конечно, видели недоброжелательное отношение крестьян к 
  себе»<sup>17</sup>. <br>
  <br>
  Итак, патриархальность быта, православное (даже православно-монархическое) миропонимание, 
  с одной стороны, определяли морально-психологическое состояние казаков, выражавшееся 
  в ненависти и жестокости к врагу, попиравшему как вековые устои казачьего быта, 
  так и то, на чем основания этого быта покоились — Церковь. С другой стороны, 
  на действия казаков в не меньшей степени влияли их сословная гордость и ненависть 
  к иногородним, посмевшим лишить казаков неограниченной власти в станицах и их 
  вековых прав на землю. Нельзя сбрасывать со счетов и то, что на протяжении веков 
  в сознании казаков вырабатывалось понимание войны как средства для захвата добычи. 
  <br>
  <br>
  Как вспоминал легендарный белый партизан А.Г. Шкуро, весной 1918 года, готовя 
  восстание и скрываясь на Волчьей поляне, он «проснулся от чьих-то устремленных 
  на меня глаз. Передо мной стоял старый казак, страшно оборванный, исхудавший 
  и босой. <br>
  <br>
  &quot;- Я — Георгий Победоносец!— заговорил он глухим голосом. — веди, молодой 
  воин, спасай казачество. Не забывай Бога, будь милосерд к людям»<sup>18</sup>. 
  Обращает на себя внимание то, что старый казак призывает молодого воина, — Шкуро, 
  <em>спасать не Россию, а именно казачество</em>, причем старец излагает некий 
  стереотип поведения казаков, призывая их не забывать Бога и быть милосердными 
  к людям. Слова старого казака, «переполненного цитатами из Святых Отцов и всевозможными 
  намеками и прибаутками, отвечали чрезвычайно настроению казаков»<sup>19</sup>. 
  Читая мемуары Шкуро, трудно понять, насколько сам он был религиозен, но православная 
  система ценностей нашла свое отражение в мышлении белого партизана: «Пасха прошла 
  спокойно. На Фоминой неделе я вышел однажды прогуляться по парку»<sup>20</sup>. 
  Той же весной, готовясь к борьбе против большевиков, Шкуро вспоминает, как приехал 
  в станицу Бургустанскую, находившуюся под властью красных, и, зайдя в станичное 
  правление, произнес перед казаками речь, призвав их к восстанию против советской 
  власти, напомнив «о тысячах невинных жизней, павших жертвами произвола и насилий, 
  и о пролитой казачьей крови, вопиющей к небу о мести»<sup>21</sup>. В другой 
  раз, в освобожденной станице Суворовской, Шкуро, принимая парад казаков, обратился 
  к ним со следующей речью: «- Поздравляю вас, кричал я, объезжая сотни. — Вы 
  опять казаки! Мноие из вас не увидят больше родной станицы, но те, которые погибнут, 
  падут за освобождение казачества!»<sup>22</sup> Обращает на себя внимание призыв 
  Шкуро мстить большевикам за пролитую казачью кровь, пасть за дело освобождения 
  <em>казачества</em> (белый партизан призывая кубанцев сложить свои головы <em>не 
  за Россию</em>, а за интересы <em>своего сословия</em>). Интересна фраза, сказанная 
  им жителям освобожденной станицы: «Вы <em>опять казаки</em>», то есть, с точки 
  зрения Шкуро, некие поступки казаков, в данном случае их служба большевикам 
  или даже непротивление Советской власти, не давали станичникам права называться 
  казаками. Можно утверждать, что белый партизан апеллировал не к национальной, 
  а к сословной гордости казаков. Но как же отреагировали бургустанцы на призыв 
  Шкуро подняться против советской власти? «Когда я кончил, <em>старики</em> бросились 
  ко мне, плача, приветствуя, обнимая»<sup>23</sup>. О том, что именно старики 
  стали опорой душой казачьего сопротивления, сообщают и красные военачальники 
  — участники Гражданской войны. В частности, Буденный вспоминал о том, как однажды 
  ему пришлось допрашивать пленного донского офицера, который утверждал, будто 
  бы пошел в армию по настоянию казаков-стариков<sup>24</sup>. Собственно, наиболее 
  стойкой частью Донски армии были казаки-старики, о которых «красный Мюрат» писал 
  даже с некоторым восхищением: «Казалось, что белые не устоят, бросят оружие, 
  сдадутся в плен. Но нет! Перед нами были <em>казаки-старики</em>, всегда предпочитавшие 
  плену смерть в бою»<sup>25</sup>. Жертвенность казаков-стариков не замыкалась 
  только на них самих. Вспоминая, как поднимали казаков в станице Бекешевской, 
  Шкуро писал: «Особенно запомнился выезд первого добровольца. На наш стук в окно 
  вышел старик-казак; он был в одном белье. Опознав Первакова (спутника Шкуро 
  — И.Х.), старик впустил нас в хату. Вошли; поднялась и хозяйка. Я сбросил бурку. 
  <br>
  <br>
  — Здравствуйте, хозяин. Я — полковник Шкуро. Начинаю войну. Давайте вашего сына! 
  Я его знаю: он добрый казак. <br>
  <br>
  — Благослови, Господи, ваше дело! Мать, иди седлай коня!»<sup>26</sup>. <br>
  <br>
  В освобожденных станицах служились молебны<sup>27</sup>, в которых казаки благодарили 
  Господа о дарованном освобождении от врага. Перед нами снова религиозный аспект 
  борьбы, который еще ждет своего исследования, но одно можно сказать твердо: 
  власть большевиков, как, впрочем, и диктатура якобинцев во Франции конца XVIII 
  в. носила ярко выраженный антихристианский характер. Тот же Буденный писал: 
  «чем больше переходило на нашу сторону трудового народа Дона, тем свирепее становились 
  <em>казаки-старики...</em> Проклиная молодых за то, что те якобы «предали Тихий 
  Дон и казачью честь», они поголовно, <em>включая девяностолетних</em>, вступали 
  в карательные отряды инквизиторского типа. Особую злобу эти «гвардейцы» контрреволюции 
  питали к иногородним крестьянам. Они врывались в станицы и хутора, рубили, резали, 
  сжигали на кострах и расстреливали ни в чем не повинных людей, не щадили ни 
  детей, ни женщин. Хутора, где жили одни иногородние, сжигались этими озверевшими 
  людьми дотла»<sup>28</sup> Отметим, что иногородние не только твердо стояли 
  за Советскую власть и служили в Красной армии, но и принимали участие в расказачивании<sup>29</sup>. 
  <br>
  <br>
  На жестокое отношение к пленным большевикам толкали казаков зверства красноармейцев, 
  о чем пишет Шкуро, вспоминая о небывалом духовном подъеме, который испытывало 
  население освобожденных им станиц: «На площади отслужили мы молебен под слезы 
  вдов и клятвы боргустанцев победить или умереть. Эта чудная станица, равно как 
  и Бекешевская (где шкуринцев также встречали с энтузиазмом — И.Х.), жестоко 
  поплатились впоследствии: они были уничтожены дотла большевиками»<sup>30</sup>. 
  <br>
  <br>
  В воспоминаниях Шкуро морально-психологическое состояние казаков предстает и 
  с другой стороны. В конце июня 1918 г. 1-й Лабинский полк, атакуя красных, ворвался 
  в станицу Баталпашинскую, однако действия лабинцев оказались неудачны, их командир, 
  по словам Шкуро, «... оправдывался тем, что Лабинский полк, состоявший из казаков, 
  незнакомых баталпашинцам, был встречен недоверчиво и не мог рассчитывать на 
  присоединение к нему местного казачества. В этом, конечно, была доля правды...»<sup>31</sup>. 
  Итак, казаки одного Кубанского войска, но расположенных на некотором расстоянии 
  станиц относились друг к другу <em>недоверчиво</em> и имели на то <em>основания</em>. 
  <br>
  <br>
  Пытаясь на страницах своих мемуаров осмыслить причины падения морали белых войск 
  в целом, казачества в частности, Шкуро пишет: «Денег (войскам — И.Х.) не отпускали 
  достаточно, даже жалованье не платилось иной раз по полгода. Приходилось жить 
  добычей, отнимаемой у большевиков. Если же таковой не попадалось или не хватало, 
  то прибегали к реквизициям у населения, уже сильно разоренного немецкой оккупацией 
  и Гражданской войной... понятия реквизиции и военного грабежа стали скоро для 
  него (населения — И.Х.) аналогичными. Казаки и солдаты быстро привыкли, в свою 
  очередь, смешивать два этих понятия, что чрезвычайно развращало армию. Нравственный 
  распад постепенно распространялся и на офицерский корпус»<sup>32</sup> — о произволе 
  власти на занятой казаками территории пишет и Елисеев<sup>33</sup>. <br>
  <br>
  По мнению белого партизана, казаки занимались грабежом только потому, что им 
  вовремя не выплачивалось жалование. Подобный вывод представляется несколько 
  странным, учитывая то, что Шкуро сам был казаком и не мог не знать менталитета 
  представителей своего сословия, для которого добыча — мощный стимул участия 
  в войне вне зависимости от выплаты жалования. Слова Шкуро о том, что казаки 
  реквизировали имущество у большевиков и только после того, как такого имущества 
  им не хватало, переходили к реквизициям у местного населения, не соответствуют 
  действительному положению дел. <br>
  <br>
  Елисеев и Шкуро (в особенности первый) погружают исследователя в эпицентр событий, 
  делая его как бы участником происходящего. Генералы Деникин и Краснов, чьи воспоминания 
  будут рассмотрены ниже, напротив, более склонны анализировать произошедшие события, 
  осмыслять их. <br>
  <br>
  Ценность воспоминаний главнокомандующего ВСЮР заключается в том, что он не только 
  описывает морально-психологическое и боевое состояние донцов, но и пытается 
  проанализировать его истоки. Вообще же главкому ВСЮР присуще верное понимание 
  роли морального фактора в Гражданской войне: <em>«в гражданской войне моральный 
  элемент более, чем где бы то ни было властвовал над всеми прочими слагаемыми 
  успеха»</em><sup>34</sup>. <br>
  <br>
  Отмечая нежелание некоторой части донских офицеров служить под знаменами генерала 
  Краснова и размышляя над причинами этого, Деникин пишет, ссылаясь на отчет начальника 
  военного и морского управлений от 1 августа 1918 г.: «за дни господства большевиков 
  (донские офицеры — И.Х.) настолько глубоко пережили стадию своего морального 
  унижения, что изверились в искренности казаков и, не веря в успех дела, постарались 
  остаться в стороне»<sup>35</sup>. Бывший главком ВСЮР обращает внимание на то, 
  что: «по-прежнему главными источниками снабжения (казаков — И.Х.) служила военная 
  добыча и не оскудевшие личные запасы домовитых зажиточных казаков»<sup>36</sup>. 
  При этом Деникин объясняет одну из причин бытовавшей у казаков (и не только 
  у донских) практики самоснабжения: «Особенно тяжело стоял вопрос снабжения Донской 
  армии, ввиду разрозненности края и разобщенности с соседними районами»<sup>37</sup>. 
  <br>
  <br>
  Большую ценность представляет осмысление Деникиным морально-психологического 
  и боевого состояния донцов. Размышляя над этим вопросом, бывший главком ВСЮР 
  приходит к интереснейшему выводу: «Донской армии по существу не было, был вооруженный 
  народ»<sup>38</sup> (мне представляется более верным определение не «вооруженный 
  народ», а «вооруженное сословие» — И.Х.). Из мемуаров Деникина следует, что 
  практически ту же самую мысль высказал генерал Краснов, выступая 1 февраля 1919 
  года на Большом круге (тогда Донской фронт стремительно рушился): «Ведь у нас 
  нет полков старого времени: у нас вооруженный народ, толпа...»<sup>39</sup><br>
  <br>
  Прежде чем прийти к подобному выводу, Деникин пишет: «Привязанность к «родным 
  станицам» и восприятие родины в пределах ... станичного юрта, дальше которого 
  не простиралось желание бороться ..., весьма смутное представление о родине 
  в широких всероссийских рамках. Была глубокая ненависть к большевикам и братание 
  (с противником — И.Х.), истинное стремление к подвигу и корысть. Высокий подъем 
  и «утомление». Беззаветное мужество и «утечка». Преданность начальникам и дисциплина 
  — не та, конечно, что проводилась новочеркасскими (т.е. Краснова — И.Х.) уставами, 
  а значительно модернизированная гражданской войной и революцией, но все же дисциплина 
  — и митингование полками и станицами, грабеж и неповиновение»<sup>40</sup>. 
  <br>
  <br>
  Почему происходило такое колебание настроения казаков? Почему так резко менялся 
  их морально-боевой дух? Деникин пытается дать ответы на эти вопросы: «фронт 
  питался все время искусственными иллюзиями — сначала о всемогущей защите императора 
  Вильгельма, потом о смене экзотическими «русскими» армиями (Южная, Астраханская, 
  Саратовский корпус), наконец, о приходе союзных дивизий. Жить долго в состоянии 
  экзальтированной мечты нельзя. Казаки будут ждать осуществления ее сначала спокойно, 
  потом с буйным нетерпением, пока, наконец, не решат: - Обман! И тогда бросят 
  фронт»<sup>41</sup>. Данная цитата во многом объясняет причину стойкости донцов 
  (прежде всего, молодых казаков и фронтовиков) и их энтузиазма, которые выражались 
  именно в такой иллюзорной вере в помощь союзников или германского императора. 
  Мысль о том, что донцы могли храбро сражаться за собственные станицы, зная о 
  том, что союзники не помогут, не имеет под собой достаточных оснований, ибо 
  и зимой 1918-1919 гг., и зимой 1919-1920 гг. донские и кубанские дивизии не 
  проявили достаточной стойкости при обороне своей земли (как раз тогда было очевидно, 
  что союзники и германцы не помогут). Вывод же бывшего главкома ВСЮР о том, что 
  донцы держались вследствие веры в помощь союзников или германцев, находит свое 
  подтверждение и в работе Краснова<sup>42</sup>, но об этом ниже. Причины колебания 
  боевого духа донцов Деникин объясняет также большевистской пропагандой: «долой 
  войну, мы вас не тронем»<sup>43</sup>. Он пишет: «То, что было выиграно в течение 
  многих месяцев моральным подъемом и оружием, в один миг было потеряно упадком 
  духа»<sup>44</sup>. <br>
  <br>
  «В казачьем настроении (в конце 1918 — начале 1919 гг., когда красные наступали 
  против донцов — И.Х.) опять наступил перелом, который умело использовала советская 
  пропаганда. Наиболее чувствительным ее аргументом были обещания советской власти 
  сохранить казачий уклад в деревнях и тщетности надежд на иностранную помощь, 
  о которой так часто и неосторожно говорил атаман (Краснов — И.Х.) колеблющимся 
  фронтам»<sup>45</sup>. <br>
  <br>
  Весь драматизм ситуации конца 1918 — начала 1919 гг., всю глубину падения морального 
  и боевого духа казаков в этот период мы видим, знакомясь с речью, произнесенной 
  атаманом Красновым на Круге 1 февраля 1919 г. и процитированную Деникиным: «Прекрасно 
  вооруженные, снабженные пулеметами и пушками отряды наши, — говорил атаман Краснов 
  на Круге, — уходят без боя в глубь страны, оставляя хутора и станицы на поругание 
  врагу. Теперь сдаются на милость красной сволочи целыми сотнями и с нею вместе 
  идут убивать своих отцов и братьев. Теперь арестовывают офицеров и старших начальников, 
  выдают их на расстрел красным, и тем подрывают в них веру в казаков и лишают 
  их необходимого мужества»<sup>46</sup>. <br>
  <br>
  Бывшего главкома ВСЮР интересует вопрос: «Чем же объясняется такой резкий перелом 
  в настроении казачества и такой глубокий ничем не оправданный развал армии?»<sup>47</sup> 
  Ответ на этот вопрос он видит в: <em>«болезни воли и духа»</em>. «Ибо Донское 
  казачество, органически чуждое коммунистическому укладу, приняв внешние формы 
  и даже практику большевизма, как личину, как средство кого-то провести и от 
  кого-то спастись, на другой же день вступало с ним в глухую борьбу всем своим 
  нутром, всем бытовым укладом, в свою очередь встречая со стороны советской власти 
  полное недоверие и прямое посягательство на жизнь казачью, быт и достояние»<sup>48</sup>. 
  <br>
  <br>
  Деникин обошел вниманием религиозный аспект борьбы казачества с большевиками, 
  который, несомненно, присутствовал в понимании хотя бы некоторой части казачества, 
  главным образом, стариков. Но бывший главком ВСЮР был, видимо, далек в отличие 
  от, скажем, генерала Брусилова, увидевшего в Гражданской войне борьбу &quot;дьявола 
  с крестом&quot;<sup>49</sup>, от религиозно-мистического понимания противостояния 
  красных и белых — такие его работы, как «Кто спас советскую власть от гибели» 
  об этом ярко свидетельствуют. <br>
  <br>
  Боевой дух донцов, причины его подъема и падения находятся в центре внимания 
  казачьего генерала Краснова — бывшего атамана Всевеликого войска Донского. Обратимся 
  к его размышлениям: «Войско Донское все, но особенно Новочеркасск, в эти весенние 
  дни 1918 года жило особенною повышенною жизнью»<sup>50</sup>. Почему же весной 
  1918 г. Дон испытывал духовный подъем? «Атамана<sup>51</sup> восстановил старинный 
  до-Петровский титул «Всевеликое войско Донское», и все войско от казака до генерала 
  подхватило этот титул. В Новочеркасске ... широко распространялась история войска... 
  В школах и гимназиях после молитв дружно пели «Всколыхнулся взволновался православный 
  Тихий Дон», и гимн этот подхлестывал, взвинчивал, как марсельеза. Была мысль 
  одеть Донскую армию в особую (отличавшуюся) от Русской форму, но этому воспротивились 
  Командующий армией генерал Денисов и Атаман. Над дворцом гордо реял сине-желто-красный 
  флаг, и Новочеркасские жители ходили им любоваться. Все это было «свое», особенное, 
  новое, принадлежащее «Всевеликому войску Донскому»<sup>52</sup>. <br>
  <br>
  Создается впечатление, что Краснов стремился пробудить в донцах не столько сословную, 
  сколько «национальную» самоидентификацию, выражавшуюся в ношении особой формы, 
  собственном знамени, собственном госаппарате и собственных вооруженных силах. 
  При этом сам Краснов заявлял, что Дону нужно «до восстановления России стать 
  самостоятельным государством...»<sup>53</sup>. <br>
  <br>
  О своей идеологии, которая, несомненно, оказывала влияние на мораль и боевой 
  дух Донской армии, бывший атаман пишет: «Большевизму Атаман противопоставил 
  шовинизм, интернационализму — национализм»<sup>54</sup>. Какой же национализм 
  противопоставил Краснов большевикам: великорусский или же псевдоказачий? Сам 
  он пишет: «Ездя по станицам и полкам, Атаман везде говорил одно: «Любите свою 
  великую, полную славы Родину — Тихий Дон и мать нашу Россию! — за веру и Родину 
  - что может быть выше этого девиза!» Само знамя донское Краснов называл переходным 
  флагом<sup>55</sup>. В чем же видит атаман причины поражения Белого дела на 
  юге России? «Как только война перестала быть национальной — народной, - она 
  стала классовой, и как таковая не могла иметь успеха в беднейшем классе»<sup>56</sup>. 
  И далее: «Если бы Деникин не изменил казакам, не оскорбил бы жестоко их молодого 
  национального чувства, они не покинули бы его»<sup>57</sup>. Странные и несправедливые 
  слова. Странные потому, что, с одной стороны, Краснов мыслил будущее казаков 
  в рамках единого Российского государства, но, с другой, он пишет об их «молодом 
  <em>национальном</em> чувстве», ощущения которого у казаков, скорее всего, все 
  же не было (особенно у уставших от войны фронтовиков и стариков, самосознание 
  которых было, несомненно, имперским). Несправедливы слова Краснова потому, что 
  именно благодаря мужеству и стойкости добровольцев зимой 1918-1919 гг. Донской 
  фронт был спасен от развала. И именно для того, чтобы спасти гибнувшие, терявшие 
  боевой дух донские корпуса, Добровольческая армия совершила в декабре 1919 г. 
  сложнейший фланговый марш с Крымско-Орловского направления к Ростову и Новочеркасску. 
  Генерал Краснов не мог об этом не знать! <br>
  <br>
  Интересно следующее размышление бывшего атамана о морали и психологии донцов. 
  Вот как формировались отряды Донской армии: «Обыкновенно после прочтения воззвания 
  и речей служили молебен и после молебна выходило на фронт (казаков — И.Х.) очень 
  много. Но по пути многие отдумывали. Других отговаривали жены. Но многие останавливались 
  под предлогом «прикурить маленько», отставали, возвращались тихонько домой. 
  <em>Шли больше старики</em> и юная зеленая молодежь, фронтовики серьезней, ждали 
  приказа и, если собирались, то «своим» полком...»<sup>58</sup>. Итак, на Дону, 
  как и на Кубани, душой и нередко главной боевой силой сопротивления большевикам 
  были казаки-старики. <br>
  <br>
  Краснов пишет о хорошем обмундировании донцов<sup>59</sup> (по его словам, недоставало 
  только сапог). Это лишний раз подтверждает вывод о том, что казаки занимались 
  грабежом не в силу плохого обеспечения, а по причине веками вырабатывавшейся 
  традиции. <br>
  <br>
  Воспоминания бывшего донского атамана противоречивы, ибо, с одной стороны, он 
  указывает на высокую дисциплину, царившую в Донской армии<sup>60</sup>, а с 
  другой, пишет, например, что одному из популярнейших донских командиров Роману 
  Лазареву, наряду с храбростью, были свойственны разгульное пьянство и веселая 
  гульба с казаками<sup>61</sup>. Интересно, что, описывая тактику донцов, Краснов 
  отмечает, будто окапываться как следует им «мешала природная казачья леность»<sup>62</sup>. 
  О том, что у донцов хромала дисциплина, свидетельствует другая цитата бывшего 
  атамана: «Казаки требовали, чтобы офицеры шли впереди. Поэтому потери в командном 
  составе были очень велики»<sup>63</sup>.<br>
  <br>
  Не обходит своим вниманием Краснов и жестокость донцов: «Так же они (казаки 
  — И.Х.) были беспощадны с пленными»<sup>64</sup>. Жесткость казаков по отношению 
  к большевикам можно объяснить и следующим образом. Известно, что большевики, 
  занимая Дон, предпринимали кощунственные действия по отношению к Церкви. Так, 
  Краснов описывает, как в занятой большевиками станице Вешенской большевики «повенчали» 
  90-летнего всеми уважаемого священника с кобылой - после подобных издевательств 
  противостояние не могло не принять религиозного характера, учитывая то, что, 
  во-первых, на Дону было много старообрядцев, во-вторых, основу казачьего сопротивления 
  во многих станицах составили представители наиболее религиозной части этого 
  военного сословия — казаки-старики. В то же время бывший атаман пишет: «Это 
  была в полном смысле народная война... Атаман и командующий армией приложили 
  все усилия к тому, чтобы внести полный порядок в организацию армии и боевые 
  действия, построить организацию на началах военной науки, добиться правильного 
  управления отрядами, не нарушая в то же время ее народного характера»<sup>65</sup>. 
  <br>
  <br>
  Краснов отмечает: к осени 1918 г. донцы неохотно шли воевать за пределы родной 
  Донской области<sup>66</sup>. Он сожалеет об этом, обращая внимание на то, что 
  его националистическая политика могла иметь для казаков, не обладавших государственным 
  мышлением, и оборотную сторону. Они, быть может, действительно стали считать 
  себя отдельным народом, живущим в действительно независимом государстве, которому 
  нет надобности воевать с соседней советской республикой. <br>
  <br>
  Утверждение Деникина, касающееся того, что на моральный дух донцов оказывала 
  существенное влияние вера в помощь союзников, находит свое подтверждение в описании 
  Красновым обеда, который был им дан в конце ноября 1918 г. в честь прибывших 
  на Дон английских и французских офицеров: «Каждое слово союзников, раздававшееся 
  здесь... звучало далеко и разносилось по самым глухим станицам и хуторам, доходило 
  до казачьего фронта. Депутаты с обеда шли на прямой провод и посылали во все 
  места телеграммы о том, что они видали, и что слышали. И смысл их телеграмм 
  был один: «Союзники с нами и за нас!..»<sup>67</sup>. Другая цитата на эту же 
  тему: «Впечатление от приезда союзников на Дону было сильное. Оно отозвалось 
  на всех фронтах. Но случилось именно то, чего так боялся Атаман. После короткого 
  подъема настроения... наступил упадок духа. Приезд союзников был допинг, был 
  наркоз, опьянивший казаков и заставивший их совершать небывалые подвиги, но 
  работать под наркозом постоянно нельзя...»<sup>68</sup>. <br>
  <br>
  К концу 1918 г. моральный дух донцов стал падать. Краснов это объяснял быстрым 
  наступлением холодов и необустроенностью позиций казаков, которые терпели лишения<sup>69</sup>. 
  Тогда же донцы стали испытывать разочарование в союзниках. По словам бывшего 
  атамана, зимой 1918-1919 гг. к передовым позициям подходили красные парламентеры 
  и призывали казаков прекратить борьбу против советской власти. Логика парламентеров 
  была проста и одновременно импонировала казакам: «Мы вашего не трогаем... зачем 
  же вы идете на нас?»<sup>70</sup>. После этого донские части начинали быстро 
  разлагаться, и офицеров, пытавшихся воспрепятствовать уходу казаков с позиций, 
  по словам Краснова, попросту арестовывали<sup>71</sup>. Примечательно, что, 
  описывая установление советской власти в станице Каргинской, Краснов вспоминает, 
  как он приехал туда с английскими и французскими офицерами и призывал казаков 
  образумиться, обещая им помощь союзников<sup>72</sup>. В данном случае трудно 
  не согласиться с Деникиным, справедливо считавшим, что Краснов питал казаков 
  иллюзорными надеждами на скорую англо-французскую помощь. Собственно, сами союзники 
  поддерживали в казаках эти иллюзии<sup>73</sup>. Краснов открыто признает, что 
  во многом настроение казаков зависело от помощи союзников<sup>74</sup>. <br>
  <br>
  По словам Краснова: «Донской Атаман не боялся побед Красной Армии, но он боялся 
  разложения Донской Армии»<sup>75</sup>. Некоторая часть казаков совершенно искренне 
  полагала, будто бы Краснов продался немцам, и что на фронт с ним ездили не союзные 
  офицеры, а переодетые казаки. 10 января 1919 г. донской атаман даже издал специальный 
  приказ, в котором рассеивал эти толки<sup>76</sup>. В одном из писем Деникину 
  Краснов прямо говорит о разложении донских войск<sup>77</sup>. <br>
  <br>
  По мнению Краснова, советская пропаганда, прежде всего, и сломила дух казаков<sup>78</sup>. 
  Так, в январе 1919 г. Краснов писал Деникину: «На Севере (Донской области — 
  И.Х.) нас побеждает не сила оружия противника, но сила его злостной пропаганды»<sup>79</sup>. 
  <br>
  <br>
  Позволим себе вернуться к мемуарам Деникина, чтобы еще раз подчеркнуть правильность 
  выводов, сделанных бывшим главкомом ВСЮР касательно причин низкого морально-психологического 
  духа донцов. Суть выводов Деникина состоит в том, что мораль донцов строилась 
  на их менталитете, на их восприятии Родины в масштабах одной «своей» станицы 
  — вспомним приведенное выше описание Шкуро недоверия одних станичников к другим. 
  Не отрицал этого и Краснов, но, делая упор на разлагающее влияние советской 
  пропаганды, он не акцентирует в достаточной степени свое внимание на том, что 
  зимой 1918-1919 гг. казаки с готовностью <em>верили</em> большевикам и потому 
  бросали позиции. <br>
  <br>
  В целом каждые из рассмотренных мемуаров могут претендовать на более или менее 
  полное освещение морально-психологического состояния и боевого духа казаков. 
  Иное дело, что Шкуро и Елисеев описывают события как бы изнутри, в их воспоминаниях 
  больше бытовых и боевых сцен, Краснов и, в большей степени, Деникин смотрят 
  на мораль и боевой дух казаков как бы со стороны. Подводя итог — разумеется, 
  в весьма ограниченных рамках статьи — рассмотрению морально-психологического 
  состояния казаков и их боевого духа, представляется возможным вновь подчеркнуть, 
  что впервые за всю свою историю они выполняли несвойственную и непривычную (оказавшуюся 
  непосильной) для себя задачу — вели самостоятельную вооруженную регулярную войну 
  как независимое государство, не обладая при этом государственным мышлением. 
  Им не обладали ни простые казаки, занимавшиеся грабежом захваченных территорий, 
  ни атаман Всевеликого войска Донского Краснов<sup>80</sup>. Замечательно о психологии 
  казаков написал Деникин: «Если для регулярных частей погоня за добычей была 
  явлением благоприобретенным, то для казачьих войск — <em>исторической традицией, 
  восходящей ко временам Дикого поля и Запорожья</em>, прошедшей красной нитью 
  через последующую историю войн и <em>модернизированной временем в формах, но 
  не в духе</em>»<sup>81</sup>. В то же время даже поверхностное рассмотрение 
  представленных мемуаров дает основание говорить, во-первых, о религиозной составляющей 
  Гражданской войны, во-вторых, о том, что на морально-психологическое состояние 
  казаков, на их боевой дух влияли презрение к иногородним, заключавшееся в уязвленной 
  сословной гордости, стремлении очистить <em>исключительно свою казачью землю</em> 
  от большевиков и нежелании освобождать русские губернии, что во многом было 
  обусловлено неприязненным отношением к казакам русских крестьян, симпатизировавших 
