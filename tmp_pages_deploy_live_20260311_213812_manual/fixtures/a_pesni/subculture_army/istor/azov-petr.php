<h1>&lt;ПЕТР I ПРИЕЗЖАЕТ НА ДОН&gt;<br>
  <br>
  НЕ ЯСЕН СОКОЛ ЛЕТАЛ ПО ПОДНЕБЕСЬЮ... </h1>
  <br>
  Не ясен сокол летал по поднебесью,<br>
  Донской есаул бегал по Дону,<br>
  Казаков-то он речью приветствовал:<br>
  &quot;Вы вставайте, добры молодцы,<br>
  Господу Богу помолитесь,<br>
  Да не пусти Господь руки варвара<br>
  На Петра, Царя Белого, православного.<br>
  Вы вставайте, други, пробудитеся.<br>
  Борзых коней, други, вы седлайте,<br>
  Под Азов город, други, поезжайте.<br>
  Сам сизый орел пробуждается,<br>
  Сам Петр Царь подымается<br>
  Со своими князьями, боярами,<br>
  Со своими донцами,<br>
  Со своими запорожцами!&quot; <br>
  <br>
  <span class='i'>Из книги генерала Петра Краснова &quot;Картины былого Тихого 
  Дона&quot; (1909), с комментарием:<br>
 <br>
  &quot;По всему Дону пошли рассказы о молодом царе. Сильное впечатление произвел 
  он на казаков. Царь был громадного роста, без 2-х вершков сажень, широкий в 
  плечах, с круглым открытым лицом и большими ясными, смелыми глазами. Он носил 
  немецкую одежду, говорил властно и в то же время приветливо. &quot;Орел, настоящий 
  орел!&quot; — в восторге говорили казаки и готовы были отдать за своего государя 
  все. И сама собою сложилась на Дону песня про первый приезд на Дон государя&quot;.</span><br>
  <br>
  <br>
  Петр I прибыл на Дон со своей армией летом 1695 г. и в союзе с донскими казаками 
  летом-осенью попытался взять Азов, принадлежавший туркам и закрывавший казакам 
  и России выход в Азовское и Черное моря. Штурм не удался, были взяты лишь две 
  сторожевые каланчи - силами казаков. Петр построил флот, на следующий год вернулся 
  с флотом под Азов и при поддержке казаков взял город.<br>

