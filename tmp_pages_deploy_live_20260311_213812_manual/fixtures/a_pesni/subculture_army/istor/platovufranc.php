	<br>
Ты Россия, ты Россия, <br>
Ты Российская земля, <br>
Много крови пролила, <br>
Много силы забрала! <br>
<br>
Платов-казак воин был, <br>
Себе бороду не брил. <br>
Когда бороду побрил, <br>
У француза в гостях был. <br>
Француз его не узнал, <br>
За купчину признавал, <br>
Со добра коня снимал, <br>
Во палату зазывал, <br>
За дубовый стол сажал, <br>
Рюмку водки наливал <br>
И купчине подносил, <br>
Об стаканчике просил: <br>
"Ты, купчина, ты, купец, <br>
Ты российский молодец! <br>
Выпей рюмку, выпей две, <br>
Скажи всею правду мне <br>
Про Платова-казака! <br>
Он в разор нас разорил. <br>
Я уж золото сорил, <br>
Много золота терял, <br>
Про Платова узнавал". - <br>
"На что золото терять? <br>
Его так можно узнать: <br>
Он - одно лицо со мной, <br>
ровно брат мене родной!" <br>
<br>
Дочь француза догадалась, <br>
Портрет начала смотреть. <br>
Платов-казак догадался <br>
Из палаты выходить, <br>
На крылечко выбегать, <br>
Будто письмецо бросать. <br>
Он не письмецо бросал, <br>
Громким голосом вскричал: <br>
"Ой вы слуги, мои други, <br>
Вы донские казаки, <br>
Вы подайте-ка коня <br>
Под Платова-казака!" <br>
<br>
На коня Платов садится <br>
И французу говорит: <br>
"Ах ворона ты, ворона, <br>
Загумённая карга! <br>
Не сумела ты, ворона, <br>
Ясна сокола поймать, <br>
Сизы перышки щипать, <br>
По чисту полю пускать, <br>
Не сумела ты Платова <br>
У себя в гостях держать". <br>
Тут француз возбунтовался, <br>
А Платов казак помчался, <br>
Только пыль столбом пошла <br>
От французова двора. <br>
