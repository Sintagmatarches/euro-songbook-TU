  Что за дом такой стоит? <br>
  Кто владелец тому дому? <br>
  Как фамилия гласит?» <br>
  <br>
  — «Это, барин, дом казенный — <br>
  Александровский централ, <br>
  А хозяин сему дому <br>
  Здесь и сроду не бывал. <br>
  <br>
  Он живет в больших палатах, <br>
  И гуляет, и поет, <br>
  Здесь же в сереньких халатах <br>
  Дохнет в карцере народ». <br>
  <br>
  — «А скажи-ка мне, голубчик, <br>
  Кто за что же здесь сидит?» <br>
  — «Это, барин, трудно помнить: <br>
  Есть и вор здесь и бандит. <br>
  <br>
  Есть за кражи и убийства, <br>
  За подделку векселей, <br>
  За кредитные билеты... <br>
  Много разных штукарей. <br>
  <br>
  Есть преступники большие,<br>
  Им не нравился закон,<br>
  И они за правду встали,<br>
  Чтоб разрушить царский трон. <br>
  <br>
  Есть за правду за народну:<br>
  Кто в шестом году восстал,<br>
  Тот начальством был отправлен<br>
  В Александровский централ. <br>
  <br>
  Отольются волку слезы.<br>
  Знать, царю несдобровать!»<br>
  Уловив слова угрозы, <br>
  Барин крикнул: «Погонять!»<br>
