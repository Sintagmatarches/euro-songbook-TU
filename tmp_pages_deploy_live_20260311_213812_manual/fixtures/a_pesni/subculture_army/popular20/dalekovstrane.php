	<span class="header"><strong>ДАЛЕКО 
  В СТРАНЕ ИРКУТСКОЙ</strong></span><br>
  <br>
  Далеко в стране Иркутской<br>
  Между скал и крутых гор,<br>
  Обнесен стеной высокой<br>
  Чисто выметенный двор.<br>
  <br>
  На переднем на фасаде<br>
  Большая вывеска висит,<br>
  А на ней орел двуглавый<br>
  Позолоченный блестит.<br>
  <br>
  По дороге тройка мчалась,<br>
  Ехал барин молодой,<br>
  Поравнявшись с подметалой,<br>
  Крикнул кучеру: «Постой!»<br>
  <br>
  «Ты скажи-ка, подметала,<br>
  Что за дом такой стоит?<br>
  Кто хозяин тому дому?<br>
  Как фамилия гласит?»<br>
  <br>
  «Это, парень, дом казенный,<br>
  Александровский централ.<br>
  А хозяин сему дому<br>
  Сам Романов Николай.<br>
  <br>
  Здесь народ тиранят, мучат<br>
  И покою не дают.<br>
  В карцер темный замыкают,<br>
  На кобылину кладут.<br>
  <br>
<em>«Сибирский архив», Иркутск, 1912, № 6<br>
  <br>
  Элиасов Л. Е. Народная революционная поэзия Восточной Сибири эпохи гражданской 
  войны. Улан-Удэ, 1957, стр. 117.</em><br>
 <br>
  <br>
  Творческое наследие Александровского централа. Первоначальный текст - стихотворение 
  «Александровский централ» - создано неизвестным каторжанином не позднее 1880-х 
  гг. Там же, в централе, к стихотворению был подобран мотив, и оно превратилось 
  в песню, которая получила известность под названием «Далеко в стране Иркутской». 
  (Существует запись от Артема Елшина, 103-х лет, колхозника с. Душелан, Баргузинского 
  аймака БМАССР. Эту песню он слышал в 1880-е гг. от заключенного Александровского 
  централа, который только что вышел на волю. Упоминание об этом см. Элиасов, 
  там же, стр. 116).<br>
  <br>
  Приведенный выше вариант – один из первоначальных, вышедших из централа. Текст 
  песни продолжал развиваться вплоть до падения монархии в 1917 году. Ненависть к самодержавию 
  высказывалась уже в первых вариантах, но в них еще нет угроз. Угрозы в адрес 
  государственного строя появятся в вариантах времен революции 1905 года.<br>
  <br>
  Есть песня с тем же началом на иной сюжет (расстрел уголовника), восходящая к &quot;Александровскому централу&quot;. Иркутск упоминается во многих каторжанских песнях - например, <a href="sirkutska.htm">&quot;С 
  Иркутска ворочуся&quot;</a>, <a href="votskoroskoro.php">&quot;Зачем я встретился 
  с тобою?&quot;</a>.<br>
  <br>
  Во время Февральской революции все политзаключенные Александровского централа 
  были освобождены. Пустовали их места недолго – в 1918-1919 гг. колчаковцы превратили 
  Александровский централ в концлагерь, где пытки и массовые расстрелы стали обычным 
  явлением. В эти годы появились две новые песни об Александровском централе на 
  тот же мотив - <a href="../grvojna/kr/valeksandr.php">«В Александровском селенье»</a> 
  о восстании заключенных в декабре 1919 года и <a href="../grvojna/kr/vvoskrmat.php">«В 
  воскресенье мать-старушка»</a>.<br>
  <br>
  Однако &quot;Далеко в стране иркутской&quot; также продолжала бытовать - у партизан 
  Восточной Сибири во время Гражданской войны, у сталинских политзаключенных, 
  затем - у диссидентов. <br>
