  <br>
  1. Далеко в стране иркутской</strong><br>
  <br>
  Далеко в стране иркутской,<br>
  Между скал, высоких гор<br>
  Обнесен большим забором<br>
  Чисто выметенный двор.<br>
  <br>
  Чистота кругом, порядок,<br>
  Нигде соринки не найдешь -<br>
  Подметалов там немало,<br>
  В каждой камере найдешь.<br>
  <br>
  Вот по дороге тройка мчится,<br>
  В ней неизвестный господин.<br>
  Он поравнялся с подметалой,<br>
  Тройку вмиг остановил.<br>
  <br>
  Шапку снял, перекрестился,<br>
  Как завидел кандалы:<br>
  «И за что вас Бог карает,<br>
  Ты, служивый, расскажи».<br>
  <br>
  «Где же, барин, все упомнишь,<br>
  Кто за что сюда попал.<br>
  Я и сам седьмое лето,<br>
  Как свободы не видал.<br>
  <br>
  Я попал сюда случайно,<br>
  За изменщицу-жену,<br>
  Что убил ее не тайно -<br>
  Знать, уж быть тому греху.<br>
  <br>
  Кто за звонкую монету,<br>
  Кто за подделку векселей,<br>
  За побег с военной службы,<br>
  За начальство - сволочей».<br>
  <br>
  «Если хочешь ты, служивый,<br>
  Я тебя освобожу». -<br>
  «Не, спасибо, добрый барин,<br>
  Я последний день сижу».<br>
  <br>
  «Ты скажи, скажи, служивый,<br>
  Что за этот большой дом,<br>
  Кто хозяин всему дому,<br>
  Как фамилия его?»<br>
  <br>
  «Это, барин, дом казенный,<br>
  Николаевский централ,<br>
  А хозяин сему дому<br>
  Сам Романов Николай».<br>
  <br>
  Барин снова сел в коляску,<br>
  Крикнул кучеру: «Пошел!»<br>
  Позади его остался<br>
  Преогромный, большой дом.<br>
