<span class='bold'>2. Кругом, кругом осиротела...<br></span>
	<br>
	Кругом, кругом осиротела,<br>
	Кругом осталась я одна.<br>
	Моё ли счастье улетело<br>
И не воротится назад.<br>
<br>
Вернись, вернись, мой разлюбезный,<br> 
Ах, к бедной девице, ко мне.
<br>
Нейдёт, нейдёт мой разлюбезный<br>
Ну, знать, спокинул он меня.<br>
<br> 
Да, знать, спокинул он, забросил,<br>
Ну, знать, другую полюбил.<br>
Сказал: «Гуляй, моя растрёпа,<br>
Но не влюбляйся ни в кого».<br>
<br>
В твоих годах любить опасно,<br>
Ну, ты засохнешь, как трава.<br>
Ну, ты засохнешь, ты завянешь,<br>
Расцвесть не сможешь никогда.<br>
<br>
Когда же роза расцветает,<br>
То всяк старается сорвать.<br>
Когда же роза засыхает,<br>
То всяк старается топтать.<br>
<br>
Когда девчонка молодая,<br>
То всяк старается любить.<br>
Когда девчонка постареет,<br>
То всяк старается забыть.<br>
<br>
