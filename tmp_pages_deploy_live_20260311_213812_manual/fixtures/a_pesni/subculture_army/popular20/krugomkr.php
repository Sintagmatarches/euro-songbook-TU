	<h1>КРУГОМ, КРУГОМ ОСИРОТЕЛА</h1>
	<br>
	Кругом, кругом осиротела,<br>
Тебя со мною, милый, нет,<br>
С тобой все счастье улетело<br>
И не воротится назад!..<br>
<br>
Вернись, вернись, мой ненаглядный,<br>
К несчастной девушке своей!<br>
Нейдет, нейдет мой ненаглядный,<br>
Нейдет, не любит он меня!<br>
<br>
Во сне, как сокол, заявился,<br>
На сердце искру заронил,<br>
Блеснул, как молния, и скрылся,<br>
Навеки счастия лишил...<br>
<br>
Сказал: «Гуляй, моя милая,<br>
Да не влюбляйся ни в кого,<br>
В твоих летах любить опасно,<br>
И ты повянешь как трава!»<br>
<br>
Когда цвет-роза расцветает,<br>
То всяк старается сорвать,<br>
Когда цвет-роза отцветает,<br>
То всяк старается стоптать!..<br>
<br>
Когда свет-девушка счастлива,<br>
То всяк старается любить,<br>
Когда свет-девушка несчастна,<br>
То всяк старается забыть.<br>
	<br>
	<span class='i'>Кастюрина М.Н. Сибирские народные песни / Ежегодник Тобольского губернского музея. Вып. III, 1895). С. 70-71. №100. Народная песня романсного склада.<br>
