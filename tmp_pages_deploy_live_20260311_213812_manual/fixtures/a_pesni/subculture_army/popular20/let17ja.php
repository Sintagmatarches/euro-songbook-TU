<h1>ЛЕТ СЕМНАДЦАТИ МАЛЬЧИШКА...</h1><br>
   Лет семнадцати мальчишка<br>
    Неженатый, холостой,<br> 
    Он задумался жениться,<br>
    Дозволенья стал просить.<br> <br> 
    - Дозволь, батюшка, жениться,<br>
    Позволь взять, кого люблю.<br>
Отец сыну не поверил, <br>
Что на свете есть любовь.<br>
<br>
Есть на свете люди разны, <br>
Можно каждую любить.<br>
Отвернулся сын, заплакал,<br>
Пошёл в садик погулять. <br>
<br>
Шёл он садиком зелёным,<br>
Шёл дорожкой столбовой,<br>
Сел под яблонькой кудрявой,<br>
Под берёзой зеленой.<br>
<br>
Вынул шашку, вынул остру<br>
И зарезал сам себя.<br>
 Его буйная головка <br>
Покатилась по траве.<br>
<br> 
Его щёчки румяные
<br> 
Против солнышка блестят,
<br> 
Его глазки голубые
<br> 
На свет белый не глядят.
<br> 
<br> 
    <span class="i">1964 г., с. Пузево Бутурлиновского р-на Воронежской обл., исп. Никольникова М.Н. 1906 г.р., зап. Умкова Ю.<br>
