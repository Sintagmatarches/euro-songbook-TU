    Лет семнадцати я, мальчишечка,<br>
  Да не женатый я, холостой, <br>
  Да не женатый я, холостой. <br>
  Ох не знал и кого-то спроситься<br>
  Да кроме матери, отца, <br>
  Да кроме матери, отца. <br>
  — Да позволь, тятенька-то, жениться, <br>
  Да позволь взять, каво я.люблю,<br>
  Да позволь взять, каво я люблю.<br>
  Да отец-то не поверил, <br>
  Что на свете есть любовь, <br>
  Что на свете есть любовь –<br>
  Как на свете все равны, <br>
  Должен всех равно любить, <br>
  Должен всех равно любить. <br>
  Отвернулся сын-то, <br>
  Отцу слова не сказал, <br>
  Отцу слова не сказал, <br>
  Да взял он востру саблю, <br>
  Да пошел в зелен сад гулять. <br>
  Его буйная головка-то <br>
  Да покатилась по траве, <br>
  Да покатилась по траве,<br>
  Его русые кудренки <br>
  Да презабрызганы в крови, <br>
  Да презабрызганы в крови, <br>
  Да голубые его глазки <br>
  На восход солнца глядят, <br>
  На восход солнца глядят.  <br>
