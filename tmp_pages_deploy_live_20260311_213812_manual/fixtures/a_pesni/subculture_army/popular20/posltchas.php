 Последний час разлуки<br>
 С тобой, мой дорогой.<br>
 Не вижу, кроме скуки,<br>
 Утехи никакой.<br>
 Ничто меня не тешит,<br>
 Ничто не веселит.<br>
 Одно есть утешенье:<br>
 Мил плакать не велит.<br>
 Гуляла я в садочке,<br>
 Гуляла в зелен<strong>о</strong>м, <br>
 Искала те следочки,<br>
 Где мил гулял со мной.<br>
 Следов не находила,<br>
 Садилась под кустом,<br>
 Садилась под кусточком<br>
 На з<strong>е</strong>лену траву. <br>
 Летели две голубки,<br>
 Одна перед одной,<br>
 Летели они, сели<br>
 На яблоньке в саду.<br>
 Одна из них спорхнула<br>
 Ко мне на белу грудь,<br>
 А я, млада, вздохнула:<br>
 О, где же милый мой,<br>
 О, где моя отрада<br>
 Сережа-пастушок?<br>
 Ходил ко мне от стада<br>
 На кр<strong>у</strong>той бережок, <br>
