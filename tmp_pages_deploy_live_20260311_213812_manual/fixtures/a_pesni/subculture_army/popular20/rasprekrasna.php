		<h1>РАСПРЕКРАСНА МОЯ НАДЯ...</h1>
<br>
Распрекрасна моя Надя<br>
Танцевала на балу.<br>
С кавалерами гуляла<br>
Назло Васе своему.<br>
<br>
Вася был такой ревнивый,<br>
Не мог сердцем утерпеть,<br>
Вызвал Надечку на улицу,<br>
Пригрозил её убить.<br>
<br>
Надя с улицы приходит,<br>
Начинает говорить:<br>
- Мама, милая, родная,<br>
Он меня хочет убить.<br>
<br>
- Ой ты, дочка, моя дочка,<br>
Что ж тут делать, как тут быть?<br>
Я давно тебе сказала -<br>
Одного надо любить.<br>
<br>
Надя с улицы приходит<br>
И садится на кровать.<br>
Вдруг в окно раздался выстрел,<br>
Не успела туфли снять.<br>
<br>
А наутро было слышно:<br>
В церкви звонят колокола -<br>
Это Надечку хоронят<br>
И уносят со двора.
<br>
