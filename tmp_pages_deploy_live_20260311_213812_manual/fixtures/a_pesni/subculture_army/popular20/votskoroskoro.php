  <strong>2. Зачем я, мальчик, уродился</strong><br>
  <br>
Зачем я, мальчик, уродился,<br>
Зачем я полюбил тебя?<br>
Ведь мне назначено судьбою<br>
Идти в сибирские края.<br>
<br>
Я в Петербурге уродился<br>
И воспитался у родных,<br>
А воровать я научился<br>
Там у приятелей своих.<br>
<br>
В Сибирь жестокую далеко<br>
Судом я в ссылку осужден,<br>
Где монумент за покоренье<br>
В честь Ермака сооружен.<br>
<br>
Пройдет весна, настанет лето,<br>
В полях цветочки расцветут,<br>
А мне, бедняжке, той порою<br>
Железом цепи закуют.<br>
<br>
Придет цирюльник с острой бритвой,<br>
Обреет правый мне висок,<br>
И буду вид иметь ужасный<br>
От головы до самых ног.<br>
<br>
Но там, в Сибири, в час полночный<br>
Свяжусь я вновь с чужим добром<br>
И, одинокий и несчастный,<br>
Пойду урманами тайком.<br>
<br>
Дойду до русской я границы,<br>
Урядник спросит: «Чей такой?»<br>
Я назову себя бродягой, -<br>
Не помню родины своёй.<br>
<br>
<em>Русский шансон / Авт.-сост. И. Банников. М.: АСТ-ПРЕСС КНИГА. - (1000 советов от газеты «Комсомольская правда»), с. 37-38.</em><br>
