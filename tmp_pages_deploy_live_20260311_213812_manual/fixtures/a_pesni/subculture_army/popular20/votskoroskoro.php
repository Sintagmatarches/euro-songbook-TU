Зачем я полюбил тебя?<br>
А мне назначено судьбою<br>
Пойти в далекие края.<br>
<br>
Вот скоро, скоро снег растает,<br>
В полях цветочки расцветут,<br>
А мне, несчастному, в то время<br>
Железом ноги закуют.<br>
<br>
Вот приведут меня в участок,<br>
Начальник спросит: «Чей такой?».<br>
А я скажу, что я бродяга,<br>
Не помнил родины своей.<br>
<br>
Придет цирюльник с бритвой острой,<br>
Обреет правый мне висок,<br>
Иметь я буду вид ужасный<br>
От головы до самых ног.<br>
<br>
Вот скоро-скоро поезд грянет,<br>
Город Иркутск невдалеке…<br>
А чья-то, чья-то дорогая<br>
Стоит и плачет в стороне.<br>
