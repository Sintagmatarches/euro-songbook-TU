  <br>
Зачем я встретился с тобою, <br>
Зачем я полюбил тебя? <br>
Назначен был тот день судьбою - <br>
Идти в далекие края.<br>
<br>
Погонят меня тогда с Расеи, <br>
Урядник спросит - кто такой? <br>
Урядник спросит - кто я был таков?<br>
<br>
Придет цирюльник с вострой бритвой, <br>
Обреет белый мой висок. <br>
Обреет белый мне висок...<br>
<br>
А с малых лет я как бродяжка, <br>
Не помню родины моей. <br>
Не помню родины моей...<br>
<br>
Пройдет весна, настанет лето,<br>
В саду все пташки запоют.<br>
В саду все ль пташки запоют...<br>
<br>
А мне несчастному железом, <br>
А мне несчастному железом, <br>
Железом ножки закуют <br>
Железом ножки закуют.<br>
<br>
<em>Песни узников. Составитель Владимир Пентюхов. Красноярск: Производственно-издательский комбинат &quot;ОФСЕТ&quot;, 1995. <br>
