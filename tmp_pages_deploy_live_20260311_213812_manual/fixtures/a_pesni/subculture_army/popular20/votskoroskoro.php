   <strong>1. Вот скоро, скоро поезд тронет</strong><br>
  <br>  Вот скоро, скоро поезд тронет,<br>
  Звонок унылый прозвонит.<br>
  Кого из нас, друг мой милый,<br>
  Кого машина потащит?<br>
  <br>
  Я в эту горькую минуту<br>
  К своей сердечной подойду,<br>
  Возьму пожму за праву руку,<br>
  Скажу: «Прощай, не забывай!».<br>
<br>
  «По тех, милый, не забуду,<br>
  Пока на свете буду жить.<br>
  А по тебе, мой расхороший,<br>
  Все буду плакать и тужить».<br>
<br>
  Пройдет зима, настанет лето,<br>
  В полях цветочки расцветут,<br>
  А мне, несчастному бродяге,<br>
  Железом ручки закуют,<br>
<br>
  Скуют мне руки, скуют ноги,<br>
  В Сибирь на каторгу сошлют.<br>
  В Сибири спросят: «Чей? Откуда?<br>
  И где же родина твоя?».<br>
<br>
  А я забывчивый бродяга,<br>
  Не знаю родины своей:<br>
  Родная мать моя – могила,<br>
  Отец – лопата и кирка.<br>
