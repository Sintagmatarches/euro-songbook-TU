<em>Припев:</em><br>
<br>
Ой, беда, ой, беда, беда, беда!<br>
Я каза…, я казачка молода.<br>
Ой, беда, ой, беда, беда, беда!<br>
Я каза…, я казачка хоть куда.<br>
<br>
Як пова…, як повадылась до хаты, <br>
Всим сэлом, всим сэлом до мэнэ сваты, <br>
Матэ ра…, матэ рада аж до нэба, <br>
Каже за…, каже замиж тоби трэба. <br>
<br>
Ой, ты, ма…, ой, ты маты, моя мыла, <br>
Лучэй ты, лучэй ты мэнэ побыла, <br>
Нэ пиду, нэ пиду за Петруся, <br>
Вин товстый и сопыть, як порося. <br>
<br>
У рябо…, у рябого, у Остапа <br>
За душой, за душой добра из кляпа, <br>
Вин як пу…, вин як пугало одитый, <br>
В голови, в голови гуляе витер. <br>
<br>
У Мыко…, у Мыколы кавалера <br>
Дуже влю…, дуже влюбчива манера, <br>
Вин зала…, вин залазэ на пынек, <br>
И цэлу…, и цэлуе всих дивок. <br>
<br>
У Степа…, у Степана красни очи, <br>
Пье гори…, пье горилку, аж хохоче, <br>
Вин за ча…, вин за чарочку вына <br>
Свою жи…, свою жинку проминя. <br>
<br>
А Даны…, а Даныла карапуза <br>
Не поба…, не побаче из-за пуза, <br>
Вин малэ…, вин малэсинький, як гныда, <br>
Луче я, луче я пиду за дида. <br>
<br>
За Дуна…, за Дунаем скрипка грае, <br>
Там каза…, там казаче мий гуляе, <br>
Все отдам за Ивана кари очи, <br>
Тильки вин, тильки вин мэнэ нэ хочэ. <br>
