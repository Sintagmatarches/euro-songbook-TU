<span class='bold'>2. Ой да, калинушка-размалинушка</span><br><br> 
	
Ой да, калинушка-размалинушка, <br>
	  Ой да, ты не стой, не стой на горе крутой –<br>
	  Ой да, не спускай листья во сине море. <br>
	  Ой да, как по синю морю, по синю морю <br>
	  Корабль плывет. <br>
	  Ой да, как во том корабле <br>
	  Три полка солдат плывет. <br>
