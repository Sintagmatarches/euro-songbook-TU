<span class='bold'>3. Калинушка ты, моя калинушка...</span><br><br>
	Калинушка ты, моя калинушка,<br>
	  С малинушкой<br>
	  Ты, червонная моя калинушка,<br>
	  Не стой, не стой<br>
	  На горе крутой<br>
	  И не спускай ты меня<br>
	  Во синее море.<br>
	  И по синему-то морю<br>
	  Кораб плыветь,<br>
	  Кораб плыветь,<br>
	  Аж волна реветь.<br>
	  А как во том-то корабле<br>
	  Три полка сидит, <br>
	  Три полка солдат<br>
	  И все молодых-то ребят.<br>
	  И генерал-то майер<br>
	  Богу молитца,<br>
	  А рядовой-то солдат<br>
	  Домой проситца:<br>
	  - Генерал, ты майер,<br>
	  Отпусти-ка меня домой,<br>
	  Отпусти-ка меня домой<br>
	  К отцу матери родной,<br>
	  И к младой-то моей-то жене<br>
	  Расхозяюшке.<br>
