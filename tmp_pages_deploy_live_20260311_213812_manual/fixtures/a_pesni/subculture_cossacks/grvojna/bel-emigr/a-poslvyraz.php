	<br><span class="text"><strong>Михаил 
  Астапенко, член Союза писателей России, <br>
        Евгений Астапенко, историк, аспирант Ростовского госуниверситета, станица 
        Старочеркасская, Ростовская обл. <br>
        </strong>
  <br>
        <span class="header"><strong>ПОСЛЕДНИЙ ВЫРАЗИТЕЛЬ ДУХА КАЗАЧЕСТВА<br>
        История нелёгкой жизни донского поэта зарубежья Николая Николаевича Туроверова 
        (1899-1972) </strong><br>
        </span> <em><br>
        &quot;Еженедельная газета писателей России&quot;, №38, Пятница, 21 сентября 
        2001 г. </em><br>
  <br>
  <br>
  Имя донского поэта, историка и журналиста Николая Николаевича Туроверова мало 
  известно на его родине, на Дону, а между тем, это был видный русский поэт зарубежья, 
  и пять томов его произведений, а главное - качество стихов говорят о масштабах 
  творчества и незаурядности личности Николая Туроверова. <br>
  <br>
  Он родился 18 (30) марта 1899 года в станице Старочеркасской в казачьей семье. 
  С детства полюбил родную станицу, на всю жизнь прикипела душа любовью к &quot;низкой 
  пристани Черкасска&quot;, к старинному собору, полынной степи. Старочеркасску 
  он посвятит затем не одно стихотворение, воспев старину, род и предков своих 
  и знаменитых уроженцев &quot;Старого города&quot;: Платова, Краснощёкова, Ефремова. 
  Обладая особым даром проникновения в прошлое, он точно увидит в стихотворении 
  &quot;Старый город&quot; живописную картину &quot;средневекового Черкасска&quot;: 
  <br>
  <br>
  На солнце, в мартовских садах, <br>
  Ещё сырых и обнажённых, <br>
  Сидят на постланных коврах <br>
  Принарядившиеся жёны. <br>
  Последний лёд в реке идёт, <br>
  И солнце греет плечи жарко; <br>
  Старшинским жёнам мёд несёт <br>
  Ясырка ? пленная татарка. <br>
  Весь город ждёт и жёны ждут, <br>
  Когда с раската грянет пушка, <br>
  Но в ожиданьи там и тут <br>
  Гуляет пенистая кружка. <br>
  А старики все у реки <br>
  Глядят толпой на половодье, <br>
  Из-под Азова казаки <br>
  С добычей приплывут сегодня. <br>
  Моя река, мой край родной, <br>
  Моих пробабок эта сказка, <br>
  И этот ветер голубой <br>
  Средневекового Черкасска. <br>
  <br>
  Беззаботное детство Коли Туроверова закончилось, когда грянула военная гроза 
  1914 года, круто изменившая судьбу России, Дона и его личную судьбу. Вспоминая 
  те дни, писал в стихотворении &quot;1914 год&quot;: <br>
  <br>
  Казаков казачки проводили, <br>
  Казаки простились с Тихим Доном. <br>
  Разве мы - их дети - позабыли, <br>
  Как гудел набат тревожным звоном?<br>
  Казаки скакали, тесно стремя <br>
  Прижимая к стремени соседа. <br>
  Разве не казалась в это время <br>
  Неизбежной близкая победа? <br>
  О, незабываемое лето! <br>
  Разве не тюрьмой была станица <br>
  Для меня и бедных малолеток, <br>
  Опоздавших вовремя родиться? <br>
  <br>
  Вскоре Туроверов поступил в Каменское реальное училище, по окончании которого 
  отправился служить в качестве вольноопределяющегося в лейб-гвардии Атаманский 
  полк. Шёл этапный для России и Дона 1917 год. Октябрьский переворот кинул Россию 
  в пучину братоубийственной войны. Фронт развалился, казаки неуправляемой массой 
  хлынули на Дон. Вместе со всеми вернулся и Николай Туроверов, устроившись в 
  Новочеркасском военном училище. <br>
  <br>
  ...Гражданская война набирала губительные обороты, и Туроверов в отряде есаула 
  Чернецова вынужден участвовать в этой братоубийственной бойне: <br>
  <br>
  С утра мы бились с конницей - на севере, <br>
  Потом - на юг - с пехотою дрались... <br>
  <br>
  - писал он, вспоминая те тяжкие дни. &quot;Вы помните эти ноябрьские дни в Новочеркасске? 
  - писал он двадцать лет спустя в рассказе &quot;Первая любовь&quot;. Это были 
  замечательные дни: Корнилов формировал Добровольческую армию, Каледин взывал 
  к казачеству. Но казаки, вернувшись с фронта, были глухи к призыву своего атамана. 
  Война им надоела, и мы - юнкера, кадеты, гимназисты, разоружив пехотную бригаду 
  в Хотунке под Новочеркасском, пошли брать восставший Ростов&quot;. А затем был 
  беспримерный по тяжести Степной поход - одно из тяжелейших испытаний, выпавших 
  на долю Туроверова. <br>
  <br>
  Дымится в Задоньи, курится <br>
  Седая февральская мгла. <br>
  Встаёт за могилой могила, <br>
  Темнеет калмыцкая твердь <br>
  И где-то правее - Корнилов, <br>
  В метелях идущий на смерть. <br>
  Запомним, запомним до гроба <br>
  Жестокую юность свою, <br>
  Дымящийся гребень сугроба, <br>
  Победу и гибель в бою, <br>
  Тоску безысходного гона, <br>
  Тревоги в морозных ночах <br>
  Да блеск тускловатый погона <br>
  На хрупких, на детских плечах. <br>
  Мы отдали всё, что имели, <br>
  Тебе, восемнадцатый год, <br>
  Твоей азиатской метели <br>
  Степной - за Россию – поход. <br>
  <br>
  - вспоминал он позже зиму 1918 года. Уже будучи офицером (подъесаулом), Николай 
  Туроверов прошёл всю гражданскую войну, получив четыре ранения и попав в конце 
  войны в Крым, где укрепились последние силы белых войск. <br>
  <br>
  Нас было мало, слишком мало, - писал он в поэме &quot;Перекоп&quot;, - <br>
  <br>
  От вражьих толп темнела даль; <br>
  Но твёрдым блеском засверкала <br>
  Из ножен вынутая сталь <br>
  ...В железном грохоте разрывов <br>
  Вскипали воды Сиваша <br>
  И ждали все, внимая знаку, <br>
  И подан был знакомый знак... <br>
  Полк шёл в последнюю атаку, <br>
  Венчая путь своих атак... <br>
  <br>
  Ожесточённая борьба шла с переменным успехом, но судьба отвернулась от белых. 
  Сплочённые дивизии красных раздавили ослабленные белые полки. Пришлось отступать... 
  С горечью писал об этом Николай Туроверов: <br>
  <br>
  В эту ночь мы ушли от погони, <br>
  Расседлали своих лошадей; <br>
  Я лежал на шершавой попоне <br>
  Среди спящих усталых людей. <br>
  И запомнил и помню доныне <br>
  Наш последний российский ночлег, <br>
  Эти звёзды приморской пустыни, <br>
  Этот синий мерцающий снег. <br>
  Стерегло нас последнее горе, <br>
  После снежных татарских полей, —<br>
  Ледяное Понтийское море, <br>
  Ледяная душа кораблей. <br>
  <br>
  В холодные дни ноября 1920 года началась эвакуация остатков армии генерала Врангеля 
  из Крыма. Вместе со всеми навсегда покидал Россию и Николай Туроверов. Это была 
  незабываемая трагедия, печать которой поэт носил в душе всю свою некороткую 
  жизнь: <br>
  <br>
  Уходили мы из Крыма <br>
  Среди дыма и огня, <br>
  Я с кормы всё время мимо <br>
  В своего стрелял коня. <br>
  А он плыл изнемогая <br>
  За высокою кормой, <br>
  Всё не веря, всё не зная,<br>
  Что прощается со мной. <br>
  Сколько раз одной могилы <br>
  Ожидали мы в бою... <br>
  Конь всё плыл, теряя силы,<br>
  Веря в преданность мою. <br>
  Мой денщик стрелял не мимо, <br>
  Покраснела чуть вода... <br>
  Уходящий берег Крыма <br>
  Я запомнил навсегда. <br>
  <br>
  Так донской казак, уроженец Старочеркасской станицы, оказался на чужбине: сначала 
  на Лемносе, потом в Югославии и, наконец, во Франции - навсегда... <br>
  <br>
  Тяжела доля эмигранта, и её в полной мере испытал Николай Туроверов, работавший 
  и лесорубом в Сербии, и мукомолом в Париже, и грузчиком в других городах Франции. 
  Но он ещё успел посещать и Сорбонну. Наблюдая эмигрантскую жизнь своих земляков, 
  Туроверов писал в 1928 году: &quot;Здесь, в Европе, казаки, шалея от восьмилетней 
  склоки и болтовни русской эмиграции, ладно пригнали к своей шее крахмальный 
  воротничок, ловко приспосабливаются к чуждой жизни; но свои рестораны называют 
  &quot;Донскими волнами&quot;, голосят в них свои песни и ругают Запад&quot;. 
  <br>
  <br>
  В спорах с казаками-сепаратистами, считавшими, что казакам надо отделиться от 
  России и создать своё государство, Туроверов был твёрдо убеждён, что &quot;без 
  России и вне России у казачества не было, нет и не может быть дорог!&quot; В 
  1928 году вышла его первая книга стихов, в 1937 и 1939 годах ? вторая и третья. 
  Четвёртая книга вышла уже в период войны в 1942 году. В 1938 году Туроверов 
  стал одним из организаторов &quot;Кружка казаков-литераторов&quot;, выпустив 
  год спустя иллюстрированный &quot;Казачий альманах&quot;. Когда в 30-е годы 
  в Париж были доставлены чудом сохранившиеся после новороссийской эвакуации ящики 
  с музейным имуществом лейб-гвардии Атаманского полка, Туроверов стал хранителем 
  музея при созданном тогда же &quot;Объединении атаманцев&quot;. При музее имелась 
  уникальная коллекция русской книги и старины, собранная генералом Д.И. Ознобишиным 
  и насчитывавшая свыше десяти тысяч томов книг и гравюр. Николай Николаевич в 
  предвоенные годы организовал и открыл в Париже интересные по подбору экспонатов 
  выставки &quot;1812 год&quot;, &quot;Суворов&quot;, &quot;Казаки&quot;. <br>
  <br>
  С началом Второй мировой войны Николай Туроверов пошёл сражаться против немцев 
  в составе 1-го кавалерийского полка французского Иностранного легиона. <br>
  <br>
  После окончания войны Туроверов возвращается в Париж, где возникает &quot;Казачий 
  Союз&quot;, секретарём которого он первоначально работает. В 1947 году Николая 
  Николаевича избирают председателем &quot;Казачьего Союза&quot;, который он возглавлял 
  до 1958 года. Казаки на чужбине снова сплотились и организовались вокруг своего 
  &quot;Союза&quot;. По инициативе Туроверова и под его редакцией с сентября 1950 
  года начинает выходить журнал &quot;Казачий Союз&quot;, распространяемый немалым 
  для такого типа издания тиражом в две тысячи экземпляров. Николай Николаевич 
  являлся одним из основателей журнала &quot;Родимый Край&quot;, начавшего выходить 
  с 1954 года. А за три года до этого в Париже по инициативе исторического общества 
  &quot;Академия Наполеона&quot; вышел сборник &quot;Наполеон и казаки&quot;, 
  составленный Н.Н. Туроверовым. Иллюстрации к нему выполнил донской казак - Пётр 
  Михайлович Поляков. Труд этот ныне является исторической и библиографической 
  редкостью. <br>
  <br>
  Усилиями Туроверова и возглавляемого им &quot;Казачьего Союза&quot; в кафедральном 
  соборе Парижа была установлена созданная казаками икона &quot;Покрова Пресвятые 
  Богородицы&quot;, как память о вынужденном пребывании донцов на чужбине. &quot;Казачий 
  Союз&quot; помогал казакам устроиться на работу, получить нужные документы, 
  переехать в другую страну. В 1965 году Н.Н. Туроверов, работавший в одном из 
  банков Парижа, вышел на пенсию, но литературного творчества не бросил. Его мысли 
  снова уносятся в Россию, на берега Дона, в родную станицу Старочеркасскую... 
  <br>
  <br>
  Больше ждать и верить и томиться, <br>
  Притворяться больше не могу. <br>
  Древняя Черкасская станица, - <br>
  Город мой на низком берегу <br>
  С каждым годом дальше и дороже. <br>
  Время примириться мне с судьбой. <br>
  Для тебя случайный я прохожий, <br>
  Для тебя, наверно, я чужой. <br>
  <br>
  В том же 1965 году в Париже вышла пятая книга стихов Туроверова, куда вошли 
  188 стихотворений, поэм и отрывков за период с 1915 до 1965 года. Два года спустя 
  нью-йоркское издательство &quot;Казачья старина&quot; выпустило небольшой сборник 
  песен всех казачьих войск, подготовленный Н.Н. Туроверовым. <br>
  <br>
  Всё чаще болезни укладывают его в больницы, всё чаще посещают его мысли о смерти, 
  о сборах в последний путь... <br>
  <br>
  Пора, мой старый друг, пора, <br>
  Зажились мы с тобою оба... <br>
  <br>
        пишет он в одном из стихотворений. Его желание - быть похороненным на 
        Дону, в родной станице. Но судьба распорядилась иначе. Прах поэта покоится 
        на русском кладбище в Сент-Женевьев-де-Буа.<br>
