        <span class="header"><strong>АНТИБОЛЬШЕВИЦКОЕ ДВИЖЕНИЕ 
        В СЕМИРЕЧЕНСКОМ КАЗАЧЬЕМ ВОЙСКЕ. КРАТКИЙ ИСТОРИЧЕСКИЙ ОЧЕРК</strong><br></span>
       <br>
        <span class="text"><em>Альманах «Белая гвардия», №8. Казачество России 
        в Белом движении. М., «Посев», 2005, стр. 225-235</em>.<br>
  <br>
  <br>
  История сопротивления Семиреченского казачьего войска большевикам отражена в 
  исторической литературе чрезвычайно фрагментарно и еще ждет своего исследователя, 
  подобного автору фундаментальной «Истории Семиреченского казачьего войска» (Верный, 
  1909) Н.В. Леденеву. В настоящем очерке предпринята попытка показать основные 
  вехи борьбы семиреченского казачества с большевизмом в период Гражданской войны 
  в России в 1917-1922 гг. <br>
  <br>
  1917 г. в жизни семиреченского казачества был очень сложным годом. Помимо крайнего 
  напряжения на Кавказском и Европейском фронтах Мировой войны, где находились 
  основные силы войска, казаки-семиреки вынуждены были ликвидировать последствия 
  кровавого киргизского мятежа 1916 г. в самом Семиречье. Практически все войско 
  находилось «под ружьем». В Семиреченской области находились в то время 3 Семиреченский 
  казачий полк, 1-я, 2-я и 3-я Семиреченские особые казачьи сотни, 1, 2, 3 и 4 
  Семиреченские ополченские казачьи сотни, а также Запасная сотня 3 Семиреченского 
  казачьего полка. Кроме этого, на границе с Китаем, был расположен 6-й Оренбургский 
  казачий атамана Углецкого полк и несколько пехотных и артиллерийских частей 
  по области. В июле-августе 1917 г. казачьим отрядам пришлось подавлять революционные 
  беспорядки в крае, устроенные в этот раз русским неказачьим населением. Вдобавок 
  ко всему, семиреченскому казачеству никак не удавалось легитимно провести выборы 
  Войскового атамана и сосредоточить всю власть в одних твердых руках. Наказной 
  атаман генерал-лейтенант М.А. Фольбаум (Соколов-Соколинский) неожиданно скончался 
  еще в октябре 1916 г., после него сменилось двое временно исполнявших должность 
  атамана, пока, наконец, 14 (27) июля Временным правительством не был назначен 
  новый атаман Генерального штаба генерал-лейтенант Андрей Иванович Кияшко (кубанский 
  казак по происхождению, бывший Наказной атаман Забайкальского казачьего войска). 
  Он прибыл в Верный как раз, в дни заседания 2 Семиреченского казачьего съезда, 
  и после обсуждения его кандидатуры, 5 (18) августа был признан семиреченским 
  казачеством Наказным атаманом, «впредь до созыва Войскового Круга». <br>
  <br>
  На этом же съезде был образован Войсковой Совет и избран его председатель, которым 
  стал хорунжий А.М. Астраханцев. Этот съезд высказался за поддержку Временного 
  правительства и укрепление связей с другими казачьими войсками. <br>
  <br>
  Генерал Кияшко, назначенный также командующим войсками области, попытался установить 
  твердый порядок в Семиречье и принял меры к расформированию большевицки настроенных 
  пехотных и артиллерийских частей и аресту зачинщиков беспорядков, но революционный 
  вал неудержимо накатывался на регион. <br>
  <br>
  В период с 28 октября (10 ноября) по 1 (14) ноября, вслед за Петроградом, произошло 
  вооруженное выступление большевиков в Ташкенте, после которого семиреченское 
  казачество открыто выступило против Советской власти. Уже 1 (14) ноября было 
  образовано Войсковое правительство (в лице Войскового атамана и Войскового правления), 
  которое вместе с Войсковым Советом заявило о своем непризнании власти большевиков, 
  установившейся в Петрограде и Ташкенте и о взятии Семиреченским казачьим войском 
  всей полноты власти в области со 2 ноября 1917 г. Было объявлено военное положение 
  и начато формирование во всех станицах и выселках добровольческих конных и пеших 
  сотен из всех, способных носить оружие казаков, с целью поддержания порядка 
  и пресечения «большевицко-хулиганских выступлений в области, откуда бы они не 
  исходили». В конце ноября Войсковым правительством было издано распоряжение 
  об отзыве из Действующей армии всех семиреченских казачьих частей, в лице которых, 
  оно надеялось обрести надежную силу для поддержания порядка, а также принята 
  попытка присоединиться к образованному в Екатеринодаре Юго-Восточному Союзу, 
  через своих делегатов в Новочеркасске. <br>
  <br>
  В то же время в области продолжал действовать Совет солдатских депутатов, проводивший 
  обширную большевицкую агитацию среди населения и официально распущенный только 
  26 декабря (8 января). Совдеп и представители либералов в Верном развернули 
  настоящую травлю против пожилого и больного генерала Кияшко, обвиняя его в издевательствах 
  над заключенными нерчинской каторги, служении царизму и т.п. В конце ноября 
  Кияшко вынужден был передать свои полномочия председателю Войскового правления 
  полковнику Н.С. Щербакову и выехать с семьей в Ташкент, откуда он собирался 
  добраться до Кубани по железной дороге. В столицу Туркестана тут же полетели 
  телеграммы из Верного, на станции Перовск Кияшко был арестован, доставлен в 
  Ташкент, а вскоре зверски убит. 30 ноября (13 декабря) Советская власть установилась 
  в Омске, 4 (17) февраля 1918 г. в Семипалатинске, в результате чего Семиречье 
  оказалось в изоляции. Подвоз продуктов извне был прекращен, почта и телеграф 
  прерваны. <br>
  <br>
  2 Семиреченский казачий полк прибыл в Верный из Персии 31 января (13 февраля) 
  1918 г. Еще в пути, при движении через охваченные большевизмом районы, полк 
  оказался распропагандированным большевиками и частично сдал оружие Самаркандскому 
  Совету. 13 (26) февраля 1918 г. на Войсковом Круге, состоялись выборы Войскового 
  атамана, и на этот пост был избран командир 2 Семиреченского казачьего полка 
  полковник Генерального штаба Александр Михайлович Ионов, а уже в ночь со 2 на 
  3 марта (н.ст.) в Верном произошло восстание большевиков с участием революционно 
  настроенных казаков 2-го полка, в результате чего органы Временного правительства 
  и Войсковой Круг были разогнаны. Некоторое время Войсковой Совет и Войсковое 
  правительство еще пытались наладить отношения с образовавшимся после переворота 
  Военно-революционным комитетом, тем более, что в марте у них появилась более 
  надежная опора чем 2-й полк — из Действующей армии в Семиречье возвратились 
  1-й Семиреченский казачий генерала Колпаковского полк, Семиреченский взвод Лейб-Гвардии 
  Сводно-Казачьего полка и 2-я Семиреченская отдельная казачья сотня. Но, почувствовав 
  свою силу, Семиреченский ВРК арестовал Войскового атамана и распустил Войсковой 
  Совет. Казаки-фронтовики, частично сдав оружие совдепу, разъехались по домам. 
  <br>
  <br>
  В середине апреля в станицы Верненского уезда был отправлен красногвардейский 
  отряд под командованием Щукина с артиллерией и пулеметами. Его задачей была 
  реквизиция хлеба и разоружение казаков. Это быстро отрезвило многих. 16 апреля 
  щукинский отряд был наголову разбит казаками станиц Софийской (Талгар), Надеждинской 
  (Иссык) и Малой Алматинской, с участием казаков 1 и 3 полков. Это послужило 
  сигналом к началу восстания пяти южных станиц Семиречья, в результате которого 
  казаками был осажден Верный. Казаки Надеждинской станицы во главе с сотником 
  Бортниковым совершили налет на город и освободили из тюрьмы Войскового атамана 
  Ионова. Обе стороны — казачья и большевицкая, встали на окраинах Верного — казаки 
  не решались войти в город, а красные — выйти за его пределы и разгромить повстанцев. 
  Бои ограничивались мелкими стычками в предместьях. <br>
  <br>
  Видя, что своими силами справиться с казачьим восстанием не удается, и желая 
  выиграть время необходимое для мобилизации сил в Семиречье и подхода помощи 
  из Ташкента, Совнарком пошел на мирные переговоры с казачеством восставших станиц 
  и 24 апреля был заключен «мирный договор». Но 11 мая, из-за несоблюдения большевиками 
  условий договора, бои вокруг Верного возобновились, а к середине месяца к городу 
  подошел красногвардейский отряд Мураева из Ташкента в количестве 600 штыков 
  с пулеметами. Он сразу же вступил в бой у станицы Любовинской (Каскелен) и вскоре 
  взял ее. После соединения отряда Мураева с местными большевицкими силами, 21 
  мая была взята Малая Алматинская станица, затем станицы Софийская и Надеждинская, 
  выселок Илийский и развернут беспощадный террор против казачества, офицеров 
  и интеллигенции. Отрядом Мураева в станицах принародно, на площадях, производились 
  расстрелы и рубки казаков, реквизировалось казачье имущество, скот и инвентарь. 
  Расстрелы производились и в тюрьмах города Верного. Это было лишь началом массового 
  антиказачьего террора, проводившегося в Семиречье еще задолго до печально известного 
  циркулярного письма-директивы Оргбюро ЦК РКП(б) от 29 января 1919 г. о расказачивании. 
  <br>
  <br>
  В начале июня 1918 г. выходит серия декретов Советской власти Семиречья по казачеству 
  (2 июня — приказ большевицкого командующего войсками области Л.П. Емелева об 
  упразднении Войскового Правления и всех станичных правлений СмКВ; 3 июня — декрет 
  №1 Семиреченского облисполкома об аннулировании навсегда сословия казаков, должности 
  Войскового атамана, Войскового правления и прочих казачьих учреждений и должностных 
  лиц, конфискации принадлежащего им имущества, инвентаря и денежных сумм; 6 июня 
  — постановление Семиреченского облисполкома о конфискации пенсионных земель 
  казачества и лишении казачества избирательных прав). Отряды разбитых и отчаявшихся 
  семиреков, во главе с атаманом А.М. Ионовым, отходили тем временем в сторону 
  китайской границы и в Северное Семиречье. По пути они очистили от большевиков 
  приграничный город Джаркент, села Хоргос и Басканчи и удерживали их до 15 июня, 
  когда к Джаркенту подошел карательный красный отряд Н.Н. Затыльникова из Верного 
  и казакам пришлось уходить за границу. <br>
  <br>
  В Кульдже, центре Илийского округа Синьцзянской провинции действовало Российское 
  консульство, и консул В.Ф. Люба помог казакам Ионова продовольствием и разместил 
  их в окрестностях города. Полковник Ионов переформировал отряд, установил телеграфную 
  связь с русским посланником в Пекине князем Н.А. Кудашевым, послами Великобритании, 
  США и Японии, а также связался с белыми властями в Омске и Семипалатинске, ходатайствуя 
  перед ними о присылке помощи в Семиречье, чтобы спасти войско от поголовного 
  истребления. Делегаты от Семиреченского войска Данченко и Шарапов пробрались 
  из Северного Семиречья в Семипалатинск и Омск, где выступили на 4-м Войсковом 
  Круге Сибирского казачьего войска. Они рассказали о событиях в Семиречье, запросили 
  помощи у братьев-сибирцев в борьбе с большевиками и собрали крупные пожертвования 
  от сибирских станиц для «младших братьев-семиреков». <br>
  <br>
  Тем временем в Семиречье начинала разгораться крупномасштабная Гражданская война. 
  С целью уничтожить «белогвардейские гнезда» на севере Семиречья, из Верного 
  туда был направлен красногвардейский отряд под командованием И.Е. Мамонтова 
  в количестве 500 штыков при двух орудиях и четырех пулеметах. Продвигаясь на 
  север, отряд Мамонтова пополнялся в пути местными крестьянами-новоселами, проводил 
  массовый террор в казачьих станицах и налагал контрибуции на их жителей. Слухи 
  о жестокостях красных опережали продвижение отряда и казаки быстро самоорганизовывались. 
  В станице Урджарской был образован Комитет спасения, арестован районный совет 
  и сформирован вооруженный отряд. Этот отряд вышел навстречу мамонтовским карателям 
  и 4 июля дал бой у селения Рыбачье на озере Алаколь, но видя, что силы неравны, 
  отступил на Урджар. <br>
  <br>
  Российский консул в Чугучаке (Синьцзян) В.В. Долбежев, хорошо зная местную обстановку, 
  телеграфировал белому командованию в Омск и Семипалатинск, прося «сделать все 
  возможное, чтобы отряды из Семипалатинска выступили возможно скорее на Сергиополь». 
  Одновременно он просит Комитет спасения удержать укрепление Бахты на границе, 
  с тем, чтобы беженцы из Семиречья могли уходить в китайские пределы. При приближении 
  Мамонтова к Урджару, 8 июля казаки без боя оставили станицу, и, перевалив хребет 
  Тарбагатай, ушли в станицу Кокпектинскую Сибирского казачьего войска, а частью 
  — на Чугучак. 9 июля Мамонтов занял Бахты. <br>
  <br>
  К этому времени, со стороны Семипалатинска, к самой северной станице войска 
  Сергиопольской (ныне — Аягуз), уже приближался авангард формируемого в Западно-Сибирской 
  армии Семиреченского отряда. Это был конный отряд подъесаула Сибирского казачьего 
  войска Г.П. Люсилина в составе партизанской сотни, сводной сотни 3-го Сибирского 
  казачьего полка и офицерской пулеметной команды (всего более 100 шашек при двух 
  пулеметах). 16 июля этот отряд ворвался на улицы Сергиополя, но не смог взять 
  крепость и казармы, где закрепились красноармейцы, и отошел от города. Люсилин 
  решил подождать подхода основных сил Семиреченского отряда, возглавлять который 
  был назначен старый семирек полковник Федор Гаврилович Ярушин. На следующий 
  день в помощь сергиопольским большевикам подошел сильный отряд под командованием 
  Иванова, в количестве 400 человек с орудием. <br>
  <br>
  20 июля, дождавшись подкрепления из Семипалатинска (автомобильный отряд капитана 
  Н.Д. Виноградова, а также семиреченские и сибирские казаки Урджарской, Кокпектинской 
  и Буконской станиц), белые перешли в наступление и 21-го числа освободили Сергиополь. 
  Гарнизон красных был частично перебит, а частично разбежался, причем первым, 
  бросив на произвол судьбы своих бойцов, бежал сам командир Иванов (впоследствии 
  он был арестован и расстрелян по распоряжению командования красных в Верном). 
  <br>
  <br>
  Освобождение Сергиополя от коммунистов вдохнуло надежды в семиреченских казаков 
  и дало мощный толчок к борьбе против большевизма по всему Семиречью. Уже 23 
  июля вспыхнуло восстание семиреков в станицах Сарканской, Лепсинской, Копальской, 
  Аксуйской, Абакумовской, Арасанской и Тополевской. 25 июля казаками-повстанцами 
  был совершен налет на самый южный город Семиреченской области — Пржевальск. 
  Местами к казакам стали присоединяться киргизы (казахи) и крестьяне-старожилы. 
  Захват белыми северных районов области поставил советскую власть Семиречья под 
  угрозу. 22 июля 1918 г. Семиреченский облисполком вынес решение об объединении 
  командования советскими отрядами оперирующими в северной части области, и о 
  создании штаба войск Семиреченского Северного фронта, чем было положено начало 
  образования Семиреченского фронта. Командующим большевицкими войсками области 
  был назначен областной военный комиссар Л.П. Емелев. <br>
  <br>
  Капитан Виноградов, взяв Сергиополь и оставив там часть отряда, с другой его 
  частью стал продвигаться по тракту на Урджар - Маканчи — Бахты. Он быстро занял 
  Урджарскую, затем Маканчи, но утром 29 июля погиб во время неожиданного столкновения 
  с мамонтовцами. В этом же бою был убит и каратель Иван Мамонтов. Руководство 
  его отрядом взял на себя его брат — П. Мамонтов, а затем Д. Кихтенко. Красные 
  снова заняли Урджар и Маканчи, но вскоре оказались зажатыми с двух сторон белыми 
  — отрядом Ярушина со стороны Сергиополя и семиреченскими казаками полковника 
  Вяткина и войскового старшины Бычкова, подошедшими из Китая и занявшими Бахты. 
  После нескольких боевых столкновений отряд Мамонтова — Кихтенко вынужден был 
  оставить тракт Сергиополь — Бахты и уходить на юг к Уч-Аралу, а затем к Саркану. 
  Белые отряды полковников Ярушина и Вяткина, соединившись в районе Урджарской 
  и выбив оттуда красных, продолжили наступление на юг области, спеша помочь восставшим 
  станицам. <br>
  <br>
  В Сергиополь тем временем подтягивались войска из Степной Сибири. В конце июля, 
  для развития наступления вглубь Семиречья, туда прибывает даже 1-й Сибирский 
  авиаотряд войскового старшины С.К. Шебалина, приданный 2-му Степному Сибирскому 
  корпусу. Вскоре сюда же перебралось из Синьцзяна Войсковое правительство Семиреченского 
  казачьего войска во главе с атаманом А.М. Ионовым и отрядом семиреченских казаков. 
  Деморализованное после подавления весеннего верненского восстания и разгрома 
  своих станиц семиреченское казачество начинает восстанавливать и сплачивать 
  свои силы. Можно сказать, что именно из Сергиополя началось возрождение войска. 
  Полные решимости освободить свою родину от большевиков, казаки-семиреки сформировали 
  в освобожденных станицах самоохранные сотни, стали вооружаться и готовиться 
  к решительному броску на юг для освобождения остальной части войска. <br>
  <br>
  Славная страница борьбы семиреков против красных — героическая оборона станицы 
  Сарканской в августе 1918 г. К этому времени большевиками уже были подавлены 
  восстания казаков в Копале, Лепсинске, Абакумовке и Тополевке. Для этой цели 
  был сформирован Верненский сводный отряд под командованием помощника областного 
  комиссара А.Я. Петренко. 8 августа после соединения в Абакумовке с отрядом Кихтенко, 
  отступившим от Уч-Арала и Саркана, объединенный красный отряд под командованием 
  Петренко насчитывал в своих рядах около 1000 штыков и 500 сабель, при 6 орудиях 
  и нескольких пулеметах. С этими силами Петренко подступил к мятежной станице 
  Сарканской и осадил ее. В Саркане находилось 520 казаков из Сарканской и Копальской 
  станиц. Руководили обороной станицы урядник Василий Королев, выбранный начальником 
  обороны и бывший офицер 2 Сибирского казачьего полка войсковой старшина Н.Д. 
  Кольц, фактически являвшийся военным руководителем. Станица была не просто окружена, 
  но наполовину захвачена красными. Противников разделяла только улица. Несмотря 
  на численное превосходство красных, белые оказывали отчаянное сопротивление. 
  При дефиците воды и боеприпасов, на солнцепеке, среди духоты и вони от разлагавшихся 
  трупов, которые лежали между противоборствующими сторонами, они держались под 
  жестоким артиллерийским и пулеметным огнем, отбивали яростные атаки, делали 
  ночные вылазки и надеялись на помощь сибиряков из Сергиополя. К концу обороны 
  люди начали слабеть, а с другой стороны улицы, красные кричали им, что никакого 
  Временного Сибирского правительства не существует и они напрасно ждут помощи, 
  предлагали сдаваться и выдать или прикончить своих руководителей. <br>
  <br>
  28 августа, после очередного штурма, войсковой старшина Кольц записал в своем 
  дневнике: «У казаков замечается упадок духа, устойчивости, ибо надеются теперь 
  на чужую помощь, а не на себя. Расход патронов за эту ночь не менее 4000. Еще 
  один такой бешеный натиск — и труды, жертвы двухнедельной борьбы сведутся к 
  нулю. Запас патронов ничтожный. Народ нервничает». Казаков, защитников станицы 
  могло спасти только чудо, но вот 29 августа с окраины станицы затрещал пулемет, 
  красные прекратили орудийную канонаду станицы и ринулись туда, а вскоре стали 
  поспешно отходить в сторону Абакумовки — на помощь осажденным подошло одно из 
  подразделений 3-го Сибирского казачьего полка из Семиреченскоro отряда полковника 
  Ярушина, уже взявшего Лепсинск. Не было слов, описать ту радость, с какой встретили 
  сарканцы своих спасителей «братьев-сибирцев». Потом, уже зимой 1918, в память 
  о спасителях-сибиряках, сарканское общество решило переименовать свою станицу 
  в Сарканско-Сибирскую. Приговор станичного общества утвердил в приказе по Семиреченскому 
  казачьему войску №70 от 19 декабря 1918 г. семиреченский атаман генерал-майор 
  А.М. Ионов. <br>
  <br>
  24 августа, когда казалось, что войска Семиреченского отряда полковника Ф.Г. 
  Ярушина вот-вот завершат освобождение Северного Семиречья, командир 2-го Степного 
  корпуса генерал П.П. Иванов-Ринов приказал начать операции по овладению Илийским 
  краем и городом Верным. Задача освобождения всего Семиречья возлагалась на 2-ю 
  Степную стрелковую дивизию (26 августа переименована в 5-ю Сибирскую стрелковую 
  дивизию) под командованием полковника (затем генерал-майора) В.П. Гулидова. 
  Однако полностью очистить от большевиков Лепсинский и Копальский уезды так и 
  не удалось.<br>
  <br>
  Действовавший в этом направлении отряд капитана Ушакова в количестве 600 штыков 
  при 2 орудиях и 4 пулеметах, еще 28 августа взял Лепсинск. Красный гарнизон 
  города ушел в село Покатиловское. Отряд Ушакова, усилив свои ряды казаками Лепсинской 
  и Сарканской станиц, 31 августа осадил Покатиловку и начал бои за ее овладение, 
  но 4 сентября, после подхода к красным сводного отряда Петренко, вынужден был 
  снять осаду и отойти к Саркану. В этих боях был убит организатор обороны Саркана 
  урядник В. Королев. 6 сентября белые были вынуждены оставить Лепсинск, но 10 
  сентября вновь освободили его, а красные части из города ушли к большому селу 
  Черкасскому, занимавшему центральное положение среди селений крестьян-переселенцев 
  Лепсинского уезда. И хотя на следующий же день Верненский сводный отряд Петренко 
  ушел из Черкасского на Гавриловку (ныне Талды-Курган), было положено начало 
  образованию крестьянского повстанческого района в тылу белых — так называемой 
  Черкасской обороны. В Черкасском и соседних с ним селах Лепсинского уезда — 
  Петропавловском, Осиновском, Колпаковском, Андреевском, Успенском и Глинковском 
  проживало в ту пору около тридцати тысяч крестьян, по большей части новоселов, 
  в основной своей массе настроенных пробольшевицки и резко антиказачьи. Уходя, 
  отряд Петренко оставил им два пулемета, несколько десятков винтовок и несколько 
  тысяч патронов. Надо отдать должное мужеству черкассцев — несмотря на почти 
  полную изоляцию от основных сил большевиков, они еще более года успешно действовали 
  в тылу белых, оттягивая на себя их силы и не позволяя ударить на Верный. Возглавляли 
  Черкасскую оборону бывшие солдаты, прапорщики и унтер-офицеры прошедшие Первую 
  мировую войну — А.Н. Дьяченко, П.Ф. Корниенко, Ф.А. Крива, Т.Г. Горбатов, С.С. 
  Подшивалов, П.И. Тузов. Им удалось организовать правильную оборону, начать собственное 
  производство оружия и боеприпасов, а также наладить эпизодическую доставку вооружения 
  с Северного Семиреченского фронта. <br>
  <br>
  Осенью 1918 г. Семиреченский фронт стабилизировался по линии песков Сымбыл-Кум 
  — Аксу — Абакумовка — Копал. Сплошной линии фронта не было, воинские части, 
  как с той, так и с другой стороны стояли в населенных пунктах, высылая в наиболее 
  важные места заставы и конные разъезды. В 100 километрах севернее линии фронта 
  находился осажденный белыми район Черкасской обороны. <br>
  Небольшая передышка между боями позволила семиреченским казакам вооружиться 
  и переформироваться в правильные воинские части. Вместо стихийно возникших повстанческих 
  станичных сотен создаются милиционные отряды и самоохранные сотни (одной из 
  первых были созданы Сергиопольская самоохранная сотня и Атаманская сотня), а 
  в сентябре был воссоздан 1 Семиреченский казачий полк, который придали 5 Сибирской 
  стрелковой дивизии. Ввиду нехватки собственно семиреченских офицеров, в него 
  была командирована группа офицеров-сибирцев во главе с есаулом А.А. Асановым, 
  назначенным командиром полка. К 13 октября 1918 г. в боевом составе полка было 
  29 офицеров, 910 шашек и 4 пулемета. Впоследствии 1 Семиреченский казачий полк 
  был переименован в 1 конный Алатавский полк Семиреченского казачьего войска, 
  были созданы еще два полка, которые позже вошли в состав Отдельной Семиреченской 
  казачьей бригады, начальником которой с 14 мая 1919 г. стал Войсковой атаман 
  генерал-майор А.М. Ионов. В октябре месяце 1 Семиреченский казачий полк, вместе 
  с 2 сотней 3 Сибирского казачьего полка и стрелковой ротой принял участие в 
  отражении атак красных, стремившихся прорвать фронт и соединиться с черкассцами. 
  Тогда большевики опрокинули казачьи заслоны и взяли Абакумовку, которую после 
  нескольких атак удалось отбить обратно только 2 декабря, после чего на фронте 
  наступило временное затишье. <br>
  <br>
  В декабре 1918 г. в Семиречье стали прибывать из Семипалатинска части Партизанской 
  дивизии атамана Б.В. Анненкова в количестве 1800 штыков и 1770 сабель при 6 
  орудиях. Белым командованием Анненкову была поставлена задача уничтожить повстанческий 
  очаг вокруг Черкасского, а затем, действуя совместно с находившимися в Семиречье 
  частями 5 Сибирской стрелковой дивизии и семиреченскими казаками, развернуть 
  наступление вглубь области и в конечном итоге овладеть городом Верным. Анненковцы 
  уже в январе 1919 г. попытались сходу взять одно из повстанческих сел - Андреевку, 
  но увязли в боях и фактически оставались в Черкасском районе до середины октября, 
  когда удалось сломить сопротивление противника и ликвидировать группировку красных 
  повстанцев. <br>
  <br>
  Партизанская дивизия атамана Анненкова постоянно пополнялась в составе, как 
  жителями Семиречья, так и соседних регионов, где действовали ее штабы пополнения, 
  набирая добровольцев и мобилизованных. Семиреченские казаки тоже служили в анненковских 
  частях, в основном в Лейб-Атаманском полку, но возможно попадали и в другие 
  части. Позже, при формировании Киргизской конной бригады (командир — полковник 
  Н.Д. Кольц), командный состав которой был частично из русских офицеров, семиреченские 
  казачьи офицеры попали в ее ряды. <br>
  <br>
  Отношения полковника Б.В. Анненкова с выборным семиреченским атаманом генералом 
  Ионовым не сложились. Они были знакомы уже давно — со времен службы Анненкова 
  в 1 Сибирском казачьем Ермака Тимофеева полку, который в мирное время стоял 
  в семиреченском городе Джаркенте. Капитан Ионов служил тогда старшим адъютантом 
  штаба войск Семиреченской области и во время инспекционных поездок часто посещал 
  Джаркент и полк Анненкова. Трудно сказать, какие были у них отношения в то время, 
  но в 1919 г. они обострились до предела, что, конечно, вредило общему делу борьбы 
  с большевизмом. <br>
  <br>
  Еще в конце 1918 г. генерал-майор Ионов решил «оказачить» все население Семиреченской 
  области. По его мысли оказачивание семиреченских крестьян необходимо было провести, 
  чтобы сгладить противоречия между ними и казаками, создать сильное и хорошо 
  управляемое войско, с которым можно было бы искоренить большевизм в Семиречье. 
  Часть крестьян-старожилов и в самом деле записалась в казаки, вызвав тем самым 
  ответную ненависть новоселов. Анненков был резко против искусственного оказачивания 
  населения, за что осуждал Войскового атамана семиреков. Конфликты между двумя 
  атаманами возникали и по другим причинам. Некоторое представление о характере 
  их взаимоотношений дает приказ Ионова по Семиреченскому казачьему войску от 
  7 августа 1919 г. и реакция на него Анненкова: «15 июля с.г. я подъезжал к Сергиопольской 
  станице вместе с членом войсковой управы есаулом Ушаковым и ординарцем урядником 
  Гражданцевым обычным способом, т.е. без конвоя, которого я, дорожа каждым бойцом 
  на фронте, никогда с собой не беру. За несколько верст до станицы на меня сделала 
  нападение сотня партизан и, внезапно окружив меня, увезли в Уч-Арал. Прапорщик 
  Волков, командовавший этой сотней, объявил, что я, по распоряжению полковника 
  Анненкова, считаюсь арестованным. Продержав меня в Уч-Арале несколько дней, 
  полковник Анненков приказал меня освободить. Во всю свою жизнь, начиная со школьной 
  скамьи, я не был арестован. Только в период большевизма я дважды удостоился 
  этой чести. Первый раз был лишен свободы большевиками, второй раз — партизанами. 
  Это совпадение наводит на мысль, что и тем и другим я, как войсковой атаман, 
  одинаково загораживаю путь. Причины того и другого ареста мне неизвестны, но 
  цели вполне ясны: и в том, и в другом случае было несомненное стремление уронить 
  меня в глазах Семиреченского казачества. Ничто в личной моей жизни не изменилось, 
  я еду туда, куда был назначен распоряжением Главнокомандующего Восточным фронтом 
  еще 10 июля. Семиреченское казачество благодаря этому случаю получит возможность 
  еще более ясно разобраться в той запутанности отношений и сложности обстановки, 
  которая царит ныне в области. Ионов». <br>
  <br>
  5 сентября, доводя до сведения войск этот приказ семиреченского атамана, Анненков 
  счел нужным внести несколько поправок и язвительных пояснений к нему в своем 
  приказе: «Свой приказ генерал Ионов заканчивает, будто едет туда, куда его назначил 
  главнокомандующий. Будем твердо надеяться, что главнокомандующий назначил генерала 
  Ионова согласно... указаний психиатра в какой-либо город, чтобы излечиться от 
  недуга. В заключение скажу, что «неустрашимый» генерал Ионов ехал на фронт под 
  фамилией Ефремова, а обратно с фронта, «дорожа каждым бойцом», кроме конвоя 
  взял с собой пулемет». <br>
  <br>
  Побывавший в Семиречье с инспекционной поездкой летом 1919 г. генерал-майор 
  Николай Петрович Щербаков, сам семиреченский казак, в докладе Совету министров 
  в Омске положительно оценил деятельность полковника Анненкова в крае. Отрицательно 
  отзывался о деятельности генерала Ионова на посту Войскового атамана и уполномоченного 
  командира Второго Степного корпуса по охране порядка в Семиреченской области 
  в своих письмах в Министерство внутренних дел в Омске гражданский управляющий 
  Семиреченской областью Балабанов. В конце концов, адмирал Колчак в октябре 1919 
  г. решил отозвать Ионова в Омск, а вскоре направил во Владивосток на должность 
  начальника штаба при Инспекторе формирований стратегического резерва генерале 
  Б.Р. Хрещатицком. Вместо него заместителем Войскового атамана Колчак назначил 
  генерал-майора Н.П. Щербакова, который, по-видимому, смог найти общий язык с 
  Анненковым. <br>
  <br>
  Боевые действия в Семиречье в летние месяцы 1919 г. свелись в основном к боям 
  вокруг района Черкасской обороны (в июле белые взяли большую часть ее территории 
  с селами Константиновским, Надеждинским, Глинковским, Колпаковским, Осиновским 
  и Андреевским), а также к отражению красных войск Северного Семиреченского фронта, 
  стремившихся прорвать фронт и соединиться с черкасскими повстанцами. В это время 
  анненковцы предприняли даже несколько «психических» атак на позиции красных. 
  <br>
  <br>
  В то же время белым командованием были сделаны попытки нанести удар во фланг 
  большевикам, в районы Джаркента, Кольджата и Пржевальска из китайского Синьцзяна. 
  Формированием белых отрядов в Западном Китае руководил прибывший в Кульджу личный 
  представитель Верховного правителя адмирала Колчака генерал-лейтенант Карцев 
  из Омска и полковник Брянцев со штабными офицерами II Степного корпуса. Активную 
  помощь им оказывали русские консулы в Кульдже (Люба) и Урумчах (Дьяков). Представителем 
  Анненкова в Кульдже с весны 1919 г. был бывший офицер 3 Сибирского казачьего 
  полка и один из организаторов свержения власти большевиков в Семипалатинской 
  области полковник Павел Иванович Сидоров. Ему удалось сколотить два мобильных 
  партизанских отряда по 400-500 человек каждый из семиреченских казаков, алашей 
  (киргизов) и таранчей. Сидорову подчинялись также отряды есаула Сапожникова 
  и капитана Бредихина. Полковнику Брянцеву удалось сформировать Отдельную Стрелковую 
  бригаду из двух полков (Текесского кавалерийского и Кольджатского или Семиреченского 
  пластунского казачьего полка). <br>
  <br>
  В течение июля-сентября 1919 г. отряды Сидорова и Карцева неоднократно прорывались 
  из Китая на советскую территорию, уничтожали мелкие большевицкие части, осаждали 
  Пржевальск и Джаркент, прикрывали отход в Китай восставших казаков и крестьян 
  Пржевальского уезда и оттягивали на себя красные силы Северного Семиреченского 
  фронта. После падения Черкасской обороны, 14-15 ноября 1919 г., отряды полковников 
  Сидорова и Брянцева предприняли большое наступление на красное Семиречье по 
  трем направлениям: 1) на Джаркент со стороны Хоргоса, 2) на Дубун и Подгорное 
  со стороны Кольджата и 3) на Пржевальск со стороны Нарынкола. Казаки Сидорова 
  прочно закрепились тогда в Хоргосе, Басканчах и Тышкане, а Брянцев занял Дубун, 
  Подгорное и Чунджу, которые удерживал до конца января 1920 г. Несмотря на то, 
  что взять Джаркент и Пржевальск так и не удалось, действия белых отрядов в этом 
  районе сковывали значительные силы красных и постоянно угрожали основным силам 
  большевиков Северного Семиреченского фронта фланговым ударом. <br>
  <br>
  К концу 1919 г. положение белых в Сибири резко ухудшилось. Под напором превосходящих 
  сил красных войска адмирала А.В. Колчака откатываются на восток и оставляют 
  Омск. В декабре 1919 г. Колчак отдает приказ о сведении всех войск, действующих 
  на Семиреченском фронте в Отдельную Семиреченскую армию, командующим которой 
  назначается генерал-майор Анненков. С падением Семипалатинска в декабре 1919 
  г. Семиреченская армия оказалась отрезанной от основных сил белых. Мало помощи 
  принесла и подошедшая через Атбасар, Акмолинск и Каркаралинск к Сергиополю Отдельная 
  Оренбургская армия под командованием Войскового атамана Оренбургского казачьего 
  войска генерал-лейтенанта А.И. Дутова. Проделав труднейший поход в сильные морозы 
  через Голодную степь, преследуемые красными частями, оренбуржцы в двадцатых 
  числах декабря хлынули в Семиречье. Это были уже разлагающиеся остатки армии, 
  голодные, обмороженные, тифозные и деморализованные. За исключением нескольких 
  частей боевой силы они уже не представляли, да вдобавок ко всему, у них тут 
  же начались конфликты с местными семиреченскими казаками, так что вскоре Дутову 
  пришлось объясняться по этому поводу с Войсковым правительством семиреков. Прокормить 
  еще 25 тысяч человек в итак испытывающем недостаток продовольствия и блокированном 
  Семиречье было крайне проблематично. Тем не менее, Анненков решил сопротивляться 
  и попытался закрепиться в Семиречье до лучших времен. 2 января 1920 г. состоялось 
  совещание высших начальников обеих армий, на котором было принято решение о 
  том, что Анненков берет на себя командование всеми вооруженными силами, а Дутов 
  принимает на себя высшее гражданское управление Семиреченским краем. После этого 
  Дутов во главе отряда в 600 сабель, составлявших его личный конвой и отдельную 
  сотню, отбыл в город Лепсинск, ставший его временной резиденцией. <br>
  <br>
  Анненков переформировал имевшиеся у него части и разделил их на три группы — 
  Северную, Центральную (Западную) и Южную. Северная группа, которую возглавил 
  генерал А.С. Бакич, состояла из остатков Оренбургской армии, сведенных в Оренбургский 
  отряд и имела в своем составе около 12,5 тысяч бойцов. Кроме того, у ней в тылу 
  находилось до 13 тысяч беженцев и множество эвакуированных оренбургских учреждений. 
  Центральной, или Западной группой командовал непосредственно атаман Анненков. 
  В ней было около 9 тысяч человек, в основном из Партизанской дивизии. <br>
  <br>
  Командующим войсками Южной группы был назначен генерал-майор Н.П. Щербаков. 
  Она состояла из 5-й Сибирской стрелковой дивизии, Алатавского и Приилийского 
  казачьих полков, 1 конного алашского полка, Семиреченского стрелкового полка, 
  Самоохранного полка, Семиреченского конного алайского полка и четырех батарей. 
  При ней же находился и Оренбургский атаман А.И. Дутов со своим отрядом. Генералу 
  Щербакову подчинялся и отряд семиреченских казаков под началом полковника Сидорова, 
  действовавший в Джаркентском районе и отделенный от основных сил горами Джунгарского 
  Алатау. С Южной группой связаны и последние успехи белых на Семиреченском фронте. 
  Так еще 7 декабря 1919 г. казаками был отбит Копал и фронт передвинулся на линию 
  Ак-Ичке — Карабулак. 15 января 1920 г. было предпринято местное наступление 
  на Гавриловку, через Ак-Ичке и село Солдатское, но после двухдневного боя белые 
  опять отошли на Копал. <br>
  <br>
  12 января большевицкие войска Кокчетавской группы 5-й Красной армии, сломив 
  сопротивление белых, взяли самую северную станицу семиреков — Сергиопольскую, 
  после чего на фронте установилось затишье. Обе стороны использовали его для 
  отдыха, перегруппировки сил и мобилизации ресурсов. Семиреченская армия была 
  в полном окружении — с севера, запада и юга были красные, а в тылу на востоке 
  — китайская граница. С наступлением более теплой погоды, в марте месяце, боевые 
  действия возобновились. К этому времени у семиреков почти истощились запасы 
  боеприпасов, ощущалась нехватка продовольствия, а реквизиции у местного населения 
  приводили к волнениям среди жителей и недовольству внутри. армии, т.к. многие 
  части состояли из местных крестьян и казаков. Становилось ясно, что удерживать 
  фронт более невозможно и надо уходить в Китай или капитулировать. <br>
  <br>
  Красные активизировались сначала на Южном участке фронта и повели наступление 
  на Копал. 4 и 5 марта они дважды пытались овладеть городом, но оба наступления 
  закончились неудачей. 22 марта наступление возобновилось, и к 24 марта большевикам 
  удалось перерезать дорогу, соединяющую Копал с тылом. Генерал Щербаков с казачьим 
  отрядом силой до 300 сабель, при 1 орудии попытался пробиться на выручку копальцам 
  со стороны станицы Арасан, но был остановлен красной кавалерийской бригадой, 
  окружен и только благодаря разыгравшейся горной метели смог вырваться из кольца 
  и уйти обратно в Арасан. Развивая наступление, красные части подошли к Арасанской 
  и 28 марта после трехчасового боя взяли станицу. Там было захвачено 200 пленных, 
  8 пулеметов и 250 винтовок. Генералу Щербакову, преследуемому красной кавалерией, 
  удалось уйти на Абакумовскую, а затем на Сарканд. Копальский гарнизон во главе 
  с заместителем командующего Южной группой, командиром Приилийского казачьего 
  полка войсковым старшиной Семеном Емельяновичем Бойко, несмотря на полное окружение 
  еще держался. <br>
  <br>
  Одновременно с Туркестанской группировкой красных наступательные операции повела 
  и Сергиопольская группа. 22 марта после упорных боев она заняла станицу Урджарскую, 
  а затем, преследуя отступающие части Оренбургского отряда, стала продвигаться 
  по Чугучакскому тракту. Бакич отступил с арьегардными боями, успев эвакуировать 
  в Китай все обозы, госпитали и учреждения. 27 марта, оставив последний населенный 
  пункт на русской земле — Бахты, он перешел границу. С ним было еще свыше 10 
  тысяч бойцов, не считая беженцев. Вместе с оренбуржцами, не желая оставаться 
  под коммунистической властью, в Тарбагатайский округ Синьцзяна ушло и много 
  семиреченских казаков из Сергиопольской и Урджарской станиц. <br>
  <br>
  Части 105 стрелковой бригады красных после взятия Урджара и Маканчей, 25 марта 
  начали наступление на поселок Рыбачье, а оттуда на Уч-Арал (Степановское), где 
  располагался штаб Анненкова. Сюда же двинулись 71 и 75 кавалерийские полки. 
  Через Джуз-Агач и Романовское они совершили рейд к Уч-Аралу с запада. 25 марта 
  Анненкову удалось отбить Уч-Арал, но, видя, что сопротивление бесполезно, Семиреченский 
  командарм отдал приказ начать отход за границу. При этом Анненков передал командование 
  тыловыми частями своей группы начальнику снабжения Семиреченской армии полковнику 
  А.А. Асанову (бывший командир 1 Семиреченского казачьего полка), который должен 
  был стянуть все части к Лепсинску и вместе с Дутовым отходить к границе. Асанов 
  не выполнил приказ Анненкова, и, более того, 27 марта издал приказ о капитуляции 
  остатков армии. 29 марта, после заключения особого договора с большевиками о 
  гарантиях безопасности и недопущении расправ, капитулировал Копал. В общей сложности 
  в Копале сдалось 1185 офицеров и казаков, в том числе Приилийский и Алатавский 
  казачьи полки из состава Семиреченской казачьей бригады. В этот же день красными 
  войсками была взята станица Абакумовская. Всего по Семиречью сдалось в плен 
  около 6 тысяч казаков и солдат. Все они, для дальнейшего разбора их судьбы, 
  были отправлены в Верный. <br>
  <br>
  Дутов со своим отрядом в 600 человек вышел из Лепсинска 29 марта, и пройдя Покатиловку 
  углубился в горы Джунгарского (Семиреченского) Алатау, направившись в так называемую 
  Сарканскую щель и намереваясь уйти в китайские пределы по единственно возможному 
  для него маршруту. Сюда же направился и Семиреченский атаман Щербаков от станицы 
  Сарканско-Сибирской. С большим трудом их отрядам удалось преодолеть труднопроходимый 
  перевал Кара-Сарык и выйти долину реки Бороталы, где они были интернированы 
  китайцами и размещены в районе селения Джимпань. Там Дутов и Щербаков простояли 
  до начала мая, после чего были переведены ближе к Кульдже, центру Илийского 
  округа. Дутов со штабом и отрядом оренбуржцев разместился в казармах русского 
  консульства в крепости Суйдун, близлежащих деревнях Мазар и Чимпанцзы, а Щербаков 
  — в Кульдже. <br>
  <br>
  Последней из России ушла Центральная группа Семиреченской армии под началом 
  самого командарма — генерал-майора Б.В. Анненкова. Его колонна в количестве 
  около 6 тысяч бойцов двинулась от Уч-Арала в сторону села Глинковского (Глиновского). 
  В составе колонны были Лейб-Атаманский, 1 Оренбургский казачий имени атамана 
  Дутова, Кирасирский, Драгунский, Конно-инженерный, Киргизско-калмыцкий конный 
  полки, а также личный конвой Анненкова с оркестром и хором трубачей, часть Маньчжурского 
  Конно-егерского полка, эскадрон полка Черных гусар, одна артбатарея, 1-я запасная 
  сотня, жандармский эскадрон и остатки ряда других частей с беженцами. <br>
  <br>
  В районе села Глинковского Анненков остановил колонну, построил все части, объехал 
  их и объявил, что желающие продолжить борьбу уходят в горы, а затем в Китай, 
  те же, кто устал, не хочет, или не может этого, могут оставаться на Родине и 
  сдаваться на милость большевиков. Он предупредил, что с его стороны не будет 
  никакого принуждения к эмиграции. В результате этого, около 1500-2000 человек 
  решили остаться, сдали оружие уходящим партизанам и стали прощаться. Затем колонны 
  разделились, и повернув в разные стороны пошли своим путем. Что произошло далее, 
  не ясно до сих пор. Однако впоследствии в урочище Ан-Агач было найдено около 
  900 трупов, а на озере Алаколь - еще 600. Советские власти утверждали, что это 
  были анненковцы, не пожелавшие уходить за кордон, порубленные и пострелянные 
  по приказу самого атамана. Но никаких убедительных доказательств не приводилось. 
  В различных советских материалах содержатся очень путаные и противоречивые данные 
  об этих событиях. Сам Анненков никогда не признавал своей вины в этой трагедии, 
  и на суде в 1927 г. категорически отрицал факт уничтожения им своих чинов, не 
  пожелавших уходить за границу. Закрадывается мысль — а не перебили ли анненковцев 
  опьяненные победой большевики, встретившие возвращавшихся белых и не пожелавшие 
  обременять себя пленными, тем более, что местность была дикая и свидетелей трагедии 
  не было!? Во всяком случае, на сегодняшний день, этот вопрос остается открытым 
  и требует дальнейшего изучения. <br>
  <br>
  С оставшимися у него частями, Анненков углубился в дикие горы хребта Джунгарского 
  Алатау, и не доходя до границы, расположился лагерем у перевала Сельке. Здесь, 
  в местности прозванной партизанами Орлиным Гнездом, отряд простоял еще около 
  двух месяцев, пока шли переговоры с китайскими властями об условиях перехода. 
  После трагического инцидента с семьями нескольких оренбургских офицеров, в котором 
  были повинны старые анненковские партизаны, от Анненкова отделился 1 Оренбургский 
  казачий полк и в количестве 500 человек ушел в Китай, к атаману Дутову. Сам 
  Анненков перешел границу 27 мая 1920 г. с отрядом в 4200 человек, спустился 
  в долину Бороталы и расположился лагерем близ Джимпани, откуда к тому времени 
  уже ушли со своими людьми Дутов и Щербаков. При переходе границы пришлось разоружиться 
  и сдать часть оружия китайцам. Часть оружия партизаны все же припрятали, надеясь 
  воспользоваться им в будущем. Партизаны назвали свой новый лагерь «Веселым», 
  поставили палатки, юрты и шалаши и стали получать небольшое довольствие от китайцев, 
  за сданное вооружение. Здесь Анненков не удерживал более своих бойцов и отряд 
  понемногу распылялся — к июлю 1920 г., перед отходом на Урумчи, в отряде оставалось 
  670 человек. <br>
  <br>
  Отряды полковников Сидорова и Брянцева, действовавшие отдельно от основных сил 
  Семиреченской армии на Джаркентско-Пржевальском направлении, изначально опирались 
  на район Кульджи, и отход их на китайскую территорию прошел более-менее организованно 
  и безболезненно. Отряд П.И. Сидорова сохранил значительную часть своего оружия, 
  базы на территории Илийского округа и нисколько не потерял боеспособность и 
  волю к продолжению борьбы. <br>
  <br>
  С уходом в Китай одних и капитуляцией других частей Отдельной Семиреченской 
  армии, белая борьба в Семиречье не закончилась — она просто приняла другие масштабы 
  и формы. Конечно, теперь уже не было крупномасштабных сражений и долговременных 
  осад городов, сел и станиц — действия свелись к вылазкам небольших повстанческих 
  и партизанских отрядов и подпольной работе офицерских и казачьих групп в захваченных 
  коммунистами уездах области. <br>
  <br>
  Уже к середине 1920 г., в Семиречье возникло несколько тайных офицерских организаций, 
  ставивших своей целью свержение Советской власти в крае. Сейчас уже трудно определить, 
  какие из этих групп действительно занимались подготовкой к восстанию, а какие 
  были искусственно созданы Семиреченской облчека, с целью спровоцировать, а затем 
  уничтожить ненадежные для новых властей элементы. Возможно, что многих из таких 
  групп вообще не существовало, а легенды о них были выдуманы чекистами, после 
  массовых репрессий против казаков в станицах, в целях оправдания своих действий. 
  Первая офицерская подпольная группа, о которой имеются сведения — это организация 
  бывшего комбрига Семиреченской армии полковника Л.В. Молоствова в Джаркентском 
  и Верненском уездах. Эта группа была быстро раскрыта чекистами, а ее участники 
  расстреляны. <br>
  <br>
  Пожалуй, что действительно существовала подпольная организация войскового старшины 
  С.Е. Бойко, служившего в областном военкомате, вместе с группой офицеров. Офицер-семирек, 
  Бойко когда-то командовал сотней во 2-м Семиреченском казачьем полку в Персии, 
  уходил в Китай с атаманом Ионовым в 1918 г., а в Северном Семиречье командовал 
  Приилийским казачьим полком. Сдавшись на милость победителей в Копале, Бойко 
  в числе других офицеров был амнистирован, и как хороший специалист направлен 
  на работу в облвоенкомат. Разъезжая по делам службы по станицам области, Бойко 
  видел последствия разрушительной работы большевиков и назревавшее недовольство 
  Советской властью не только среди казаков, но и среди прочего русского населения, 
  в том числе и среди солдат-красноармейцев. Дело в том, что новая власть после 
  разгрома белых на Северном Семиреченском фронте, решила привлечь на свою сторону 
  инородцев из числа из числа киргизов (казахов), таранчей (уйгуров) и дунган. 
  Заигрывая с ними и делая опору на национальные кадры, она конфисковывала земли 
  у русского населения, как у «колонизаторов» и передавала «угнетенным при царском 
  режиме» инородцам. Многие из них стали вступать в РКП(б) и занимать руководящие 
  посты в области, более всего насторожила крестьян попытка создания национальных 
  частей из мусульманского населения. При непрекращающихся реквизициях хлеба, 
  теперь уже в переселенческих селах, это послужило поводом к мятежу Верненского 
  гарнизона в июне 1920 г. И хотя он был подавлен, с помощью тех же мусульманских 
  частей, брожение среди русского населения не прекращалось. <br>
  <br>
  В этих условиях Бойко смог сколотить крепкую подпольную организацию, численность 
  которой по некоторым оценкам доходила до 660 человек. Отделения организации 
  действовали в станицах Надеждинской, Софийской, Большой Алматинской и Джаланашской, 
  а также в некоторых крестьянских селах. В самом Верном, наряду с Бойко ее возглавлял 
  и бывший капитан анненковского полка Черных гусар Александров, капитан Кувшинов, 
  штабс-капитан Воронов, поручики Покровский и Сергейчук. Им удалось связаться 
  с находившимся в Китае Дутовым и разработать детальный план восстания в Верном, 
  Джаркенте и южных станицах. Одновременно Дутов должен был вторгнуться из Синьцзяна 
  и совместно с повстанцами очистить область от большевиков. <br>
  <br>
  Деятельность бывших анненковцев не осталась незамеченной для ЧК. В группу Бойко 
  был внедрен тайный агент большевиков, и осенью 1920 г., накануне намечавшегося 
  выступления, Бойко и его штаб были арестованы. По станицам прокатилась очередная 
  волна террора, в ходе которого было репрессировано 1800 человек. Кое- какие 
  станичные дружины сумели оказать сопротивление большевикам, но силы были неравны 
  и казакам пришлось уйти в горы, или, соединившись с прорвавшимся из-за кордона 
  отрядом полковника Сидорова, отходить за границу. Зачистка станиц продолжалась 
  до апреля следующего года. Бойко же, и его соратники были увезены в Ташкент 
  и расстреляны в июне 1921 г. <br>
  <br>
  В ноябре 1920 г. в Нарынском укреплении произошел мятеж красноармейского гарнизона, 
  возглавлявшийся амнистированными офицерами Демченко и Кирьяновым. Им удалось 
  ликвидировать в Нарыне Советскую власть и начать наступление на Пишпек, но столкнувшись 
  с советским полком особого назначения повстанцы вынуждены были отойти обратно 
  в Нарын, а потом и в Китай. Тогда же, в ноябре 1920 г., областная ЧК сообщила 
  о раскрытии еще одной подпольной организации полковника Нилова в районе озера 
  Балхаш. <br>
  <br>
  Среди ушедших в эмиграцию казаков, наибольшую активность в борьбе с большевиками 
  проявлял Оренбургский атаман генерал-лейтенант А.И. Дутов и командир партизанского 
  отряда семиреков полковник П.И. Сидоров. Дутов, обосновавшийся в Суйдуне, и, 
  благодаря денежным переводам с Дальнего Востока, устроивший более-менее сносную 
  жизнь своего отряда, наладил хорошие отношения с джен-шеу-ши (военным губернатором) 
  Илийского округа Синьцзяна, установил связи с подпольными организациями Семиречья, 
  послал связных в Фергану к басмаческому командиру Иргашу, и попытался объединить 
  под своим началом все белые силы в Западном Китае. Конечной целью его было собрать 
  достаточно сильный отряд в районе Кульджи, хоть как-то вооружить его и ударить 
  на Джаркент, одновременно подняв восстание в Семиречье. В том, что это возможно, 
  его убеждали произошедшие мятежи красноармейских гарнизонов в Верном и Нарыне. 
  И кто знает, как бы сложились обстоятельства, если бы не траги ческая гибель 
  его от рук красных террористов, устроивших покушение на Войскового атамана оренбургского 
  казачества 6 февраля 1921 г. <br>
  <br>
  После смерти Дутова командование над его отрядами в Суйдуне, Мазаре и Чимпанцзах 
  взял на себя полковник Ткачев, вскоре его сменил полковник Гербов. Но ни тот, 
  ни другой не смогли удержать отряды от распыления — часть казаков ушла к Бакичу 
  в Чугучак, часть — на Дальний Восток, а над оставшимися в Синьцзяне людьми, 
  взял руководство начальник штаба Дутова полковник Павел Петрович Папенгут. Последнему 
  впоследствии, уже в начале 1930-х гг., довелось сыграть выдающуюся роль в политической 
  истории Синьцзяна и белой эмиграции и тоже трагически погибнуть на своем посту. 
  <br>
  <br>
  Полковник Павел Иванович Сидоров, формально подчинявшийся Анненкову, оказавшись 
  в одном районе с Дутовым, установил с ним хорошие деловые отношения. Располагая 
  мобильным, боеспособным и организованным отрядом из семиреченских казаков, превосходно 
  знавших местность и имевших тесные связи с местным населением, он наводил ужас 
  на советские власти в приграничных районах. Неожиданно появившись с гор, или 
  из зарослей вдоль реки Или, сидоровцы уничтожали советские учреждения в селах 
  и станицах, нападали на продотряды и команды красноармейцев и столь же неожиданно 
  исчезали, прежде чем противник успевал опомниться и организовать преследование. 
  <br>
  <br>
  Когда осенью 1920 г. чекисты начали масштабную чистку в семиреченских станицах, 
  после разгрома бойковской организации, и в Китае появились новые беженцы, Сидоров 
  решил, что его час пробил, и настала пора действовать. В конце 1920 г. он возглавил 
  рейд на территорию РСФСР и, прорвавшись через пограничные заслоны, вышел к казачьим 
  станицам. Против его отряда красные выслали свою самую маневренную и надежную 
  часть — дунганский полк Магаза Масанчи. В районе Джаланаша произошел бой. Коммунисты 
  бросили в помощь кавполку Масанчи дополнительные силы, и полковнику пришлось 
  отойти. На Аккентской дороге близ Джаркента большевики попытались окружить и 
  уничтожить отряд, но казаки прорвались и ушли на китайскую территорию. Несмотря 
  на то, что поход окончился неудачей, и раздуть пламя восстания в Семиречье не 
  удалось, воинственный полковник и не думал складывать оружия. <br>
  <br>
  В 1921 г. положение белоэмигрантов в Китае значительно ухудшилось. Так как китайцы 
  перестали признавать старых российских консулов, в мае 1920 г. из Кульджи на 
  восток отбыл так много сделавший для белых консул В.Ф. Люба, а в ноябре того 
  же года — чугучакский консул В.В. Долбежев, и русским беженцам уже не к кому 
  было обращаться за защитой. В 1921 г., в городах Синьцзяна, в т.ч. в Кульдже, 
  открылись торговые представительства РСФСР, а под их вывеской обосновались агенты 
  ЧК. В связи с захватом Урги, и изгнанием оттуда китайцев генерал-лейтенантом 
  бароном Р.Ф. Унгерном, китайские власти стали очень подозрительно относиться 
  к русским белым и в Синьцзяне. Теперь партизанам, чтобы избежать ареста, приходилось 
  скрываться и от китайских властей и от советских агентов. <br>
  <br>
  Как представляется, первоначально Сидоров входил в подчинение преемнику Дутова 
  полковнику Гербову, пока тот пытался координировать действия нескольких белых 
  отрядов из казаков и алашей на территории Семиречья и в районе русско-китайской 
  границы. Под общим командованием Гербова весной 1921 г. было около 3050 сабель. 
  На территории Лепсинского уезда, в районе перевалов Сельке и Чулак действовал 
  круп ный и хорошо подготовленный отряд под командованием полковника Белянина, 
  уроженца Уч-Арала, хорошо знавшего эти места. В этом отряде, численностью по 
  различным оценкам от 500 до 1000 человек, в основном были местные киргизы (казахи), 
  под руководством русских офицеров. В свое время еще Дутов возлагал большие надежды 
  на алашский отряд Белянина. Он предполагал двинуть в помощь Белянину Атаманский 
  полк оренбуржцев под командой полковника Е.Д. Савина из Чугучака, совместно 
  выйти к станицам и поднять восстание среди казаков Лепсинского уезда, закрепиться 
  и ждать подхода основных сил Бакича из Чугучака и Дутова из Суйдуна. По разным 
  причинам, Савин не соединился тогда с Беляниным, оставшись в Чугучаке. В долине 
  реки Боротала, ориентируясь на советскую территорию, находился отряд капитана 
  Козлова в 500 сабель. Полковник Белянин во время похода по Семиречью был выкраден 
  чекистами, вместе с несколькими своими офицерами, а его отряд к маю 1921 г. 
  разбит и частично ушел в Китай. Сам Белянин погиб в красном плену. В течение 
  всего 1921 г. совершали набеги на русскую сторону небольшие отряды есаула Остроухова, 
  Мартемьянова и Аверьянова. Они прощупывали советское приграничье, вели разведку 
  и пытались достать спрятанное во время отхода за кордон полковое имущество и 
  оружие. <br>
  <br>
  Решив покончить с остававшимися на территории Синьцзяна белыми частями и сибирскими 
  повстанцами из Народной дивизии, которые прорвались в Тарбагатайский округ с 
  территории РСФСР, советские власти, по договоренности с китайцами, двинули на 
  Чугучак в мае 1921 г., части Красной армии под командованием В.Г. Клементьева. 
  И хотя основным силам генерал-лейтенанта А.С. Бакича удалось тогда уйти на северо-восток, 
  в Алтайский округ, в районе Чугучака в плен красным попало около 1200 белогвардейцев 
  и беженцев, в том числе два бывших комбрига - генерал-майор Ф.Г. Ярушин и полковник 
  П.И. Виноградский. Все они были уведены на советскую территорию, и дальнейшая 
  судьба их неизвестна, но, вероятно, многие были расстреляны. По всей видимости, 
  среди этих несчастных было немало семиреченских казаков. <br>
  <br>
  После убийства Дутова (февраль 1921), отъезда на восток, а затем ареста китайцами 
  Анненкова (март 1921), разгрома Бакича (осень 1921), полковник Сидоров остался 
  единственным крупным белым вождем в Западном Китае, который пытался активно 
  бороться с красными (Щербаков держался пассивно). В целях конспирации он распустил 
  слухи о своей гибели и перешел на нелегальное положение. Под чужим именем полковник 
  стал работать в небольшой кузнице в Кульдже. Эта кузница стала подпольным штабом 
  не смирившихся с новой властью белогвардейцев. <br>
  <br>
  Полковник Сидоров стал разрабатывать детальный план вторжения на советскую территорию 
  из района Кульджи и поднятия там восстания из казаков и крестьян. Обстановка 
  в Семиречье внушала ему надежду на успех, и, хотя к концу 1921 г. крестьянские 
  волнения в стране пошли на убыль, сильное недовольство политикой советских властей 
  сохранялось. Как отмечалось выше, особенное возмущение вызывала советская политика 
  опоры на местные национальные кадры в противовес русским, и как следствие, усиленное 
  политическое и экономическое давление на последних. В 1921 г., в угоду инородцам, 
  был переименован даже центр области, который из Верного превратился в Алма-Ату. 
  <br>
  <br>
  План похода в Советскую Россию окончательно созрел у Сидорова к середине 1922 
  г. Он предполагал участие в этой операции китайского генерала И Тайджу — бывшего 
  командира Маньчжурской бригады Семиреченской армии. И Тайджу, бывший к тому 
  времени комендантом крепости Куре, согласился участвовать в этой операции, и 
  вроде бы даже согласовал ее план с Анненковым, посетив его в тюрьме города Урумчи. 
  По сигналу Сидорова И Тайджу должен был со своими людьми разоружить китайский 
  гарнизон Куре, завладеть местным арсеналом, в котором хранилось все изъятое 
  оружие анненковских и дутовских частей (несколько орудий, 80 пулеметов, 8 тысяч 
  винтовок и др.), а затем вооружить им казаков Сидорова. Вместе они должны были 
  перейти границу и взять Джаркент, гарнизон которого по сведениям разведки был 
  на тот момент слаб. Превратив город в базу повстанческой армии, предполагалось 
  начать боевые действия в Семиречье, подняв на борьбу местных казаков и крестьян. 
  Общая численность отряда на первоначальном этапе предполагалась примерно в 10 
  тысяч человек. <br>
  <br>
  Однако, как уже не раз бывало, белые недооценили работу советских спецслужб. 
  Втершийся в доверие к полковнику бывший прапорщик его отряда Касымхан Мухамедов, 
  дезертировавший и завербованный ЧК еще в 1920 г., сумел организовать покушение 
  на него. Он дезинформировал Сидорова о прибытии в Кульджу посланцев от басмаческого 
  вожака Курширмата из Ферганы, которых давно ждал полковник. На первую встречу 
  Сидоров пришел с охраной, и Мухамедов не осмелился ничего предпринять. Отсутствие 
  связных от Курширмата он объяснил тем, что, увидев в доме много людей, они побоялись 
  войти, и потребовал новой встречи, уже без охраны. К несчастью Сидоров слишком 
  доверял своему бывшему прапорщику, и, согласившись на новую встречу, был зарезан 
  в доме Мухамедова в ночь с 15 на 16 августа 1922 г. Мухамедов и другой агент 
  — Эрса Юсуфходжаев, удостоверившись в кончине полковника, вскочили в седла и 
  поскакали к границе. Попытка группы казаков настигнуть убийц, не увенчалась 
  успехом. Перейдя границу и проникнув в Джаркент, они уже не нашли там Мухамедова, 
  который был спешно отправлен ГПУ вглубь советской территории. <br>
  <br>
  Так, не дожив до сорока лет, погиб один из наиболее выдающихся и доблестных 
  казачьих командиров, пытавшийся, несмотря ни на что, бороться с захватившими 
  страну коммунистами. И хотя П.И. Сидоров не был казаком по рождению (он происходил 
  из дворян Санкт-Петербургской губернии), именно он смог объединить и организовать 
  семиреченских казаков на последнем этапе борьбы с большевиками, вдохнуть в них 
  силы и веру в победу и стать их бесспорным военным лидером. После его гибели 
  идея организации большого похода в Россию фактически сошла на нет. Видя, что 
  Синьцзян наводнен агентами ГПУ, и не став ждать судьбы Дутова и Сидорова, двинул 
  на восток с небольшим отрядом казаков семиреченский атаман генерал-майор Н,П. 
  Щербаков. Пересекая пустыню Гоби, он заболел пятнистым тифом и умер в городе 
  Сюй-Джоу 15 сентября 1922 г. Часть казаков его отряда добралась до Шанхая, где 
  впоследствии была небольшая Семиреченская казачья станица. <br>
  <br>
  Говоря об участии семиреченских казаков в Гражданской войне, южно упомянуть 
  еще о части Лейб-Атаманского полка Анненкова, которой удалось повоевать в Белом 
  Приморье в 1922 г. Вероятно, среди атаманцев было немало семиреченцев. Этот 
  отряд под командованием полковника П.Д. Иларьева, пройдя весь Китай, прибыл 
  в апреле 1922 г. в Приморье, где бился с красными под названием Анненковского 
  дивизиона до ноября месяца. 2 ноября 1922 г. дивизион пересек русско-китайскую 
  границу вместе с основными силами Земской рати и был интернирован близ города 
  Хунчун. Численность отряда к 1 сентября 1922 г. была небольшой — 287 штыков. 
  Кроме того, во Владивостоке, у бывшего однополчанина Анненкова по 1 Сибирскому 
  казачьему полку генерала Ф.Л. Глебова, было еще 35 анненковцев, но трудно сказать, 
  были ли среди них семиреки. <br>
  <br>
  Твердо вставшая на ноги Советская власть в Семиречье после ликвидации всех своих 
  врагов в начале 1920-х немного «отпустила вожжи», а затем, к началу коллективизации 
  вновь свирепо принялась уничтожать своих потенциальных противников, явных и 
  мнимых. В 1928 г. по Джетысуйской губернии (бывшая Семиреченская область) прокатились 
  волнения в казачьих станицах, связанные с вывозом хлеба. Снова начались аресты 
  тех, кого не добрали в Гражданскую. В период коллективизации были раскулачены 
  и высланы со своей родины уже не только казаки, но и массы русских крестьян, 
  бывших непримиримых врагов казачества. Тогда из Семиречья и соседних областей 
  был еще один исход в Синьцзян, кое-где снова были попытки поднять знамя повстанческой 
  войны. Последняя известная нам попытка вооруженной борьбы с коммунистами в восточном 
  Казахстане — это нападение из-за кордона на пограничную Матвеевскую заставу 
  на Алтае, в апреле 1932 г. В этой акции приняли участие некоторые бывшие чины 
  Оренбургского корпуса генерала Бакича. Сейчас неизвестно, были ли это оренбуржцы, 
  сибирцы, семиреки или просто русские крестьяне, осознавшие всю губительность 
  нового строя. <br>
  <br>
  Советская власть методично, годами уничтожала даже память о казачьем Семиречье, 
  стирая с географических карт исконные наименования станиц, поселков и городов. 
  В 1968 г. с карты Семиречья исчезло последнее казачье поселение, носившее официальное 
  название «станица» - Иссыкская, ставшее городом Иссык. Нынешние власти Казахстана, 
  продолжая политику коммунистов, искажая исторические факты, также вытравливают 
  из памяти народа все, связанное не только с казачеством, но уже и с пребыванием 
  русских на этой земле. Современное Семиречье полностью изменило свой этнический 
  облик — в бывших станицах звучит другая речь, и все меньше в них встречается 
  не только казаков, но и просто русских людей. <br>
