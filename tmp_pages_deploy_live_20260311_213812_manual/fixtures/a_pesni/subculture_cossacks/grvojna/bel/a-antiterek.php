        Г. Гагкуев </strong><br>
  <br></span>
        <span class="header"><strong>АНТИБОЛЬШЕВИЦКОЕ ДВИЖЕНИЕ В ТЕРСКОМ 
        КАЗАЧЬЕМ ВОЙСКЕ. КРАТКИЙ ИСТОРИЧЕСКИЙ ОЧЕРК</strong><br></span>
  <span class="text"><br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 102-106</em>.<br>
  <br>
  <br>
  Терское казачье войско, с центром во Владикавказе, размещалось на территории 
  Терской области Российской Империи, на месте которой в настоящее время находится 
  южная часть Ставропольского края, Кабардино-Балкарская Республика, Республика 
  Северная Осетия — Алания, Республики Чечня и Ингушетия, а также северная часть 
  Дагестана. <br>
  <br>
  С 1905 г. Терская область делилась на четыре казачьих отдела: Пятигорский, Моздокский, 
  Сунженский и Кизлярский (всего 70 станиц), и шесть национальных округов и подчинялась 
  начальнику области, являвшемуся также и Наказным атаманом. Население области 
  на 1916 г. составляло 1,36 млн. человек, из которых казаки составляли 255 тысяч. 
  Войско располагало 2,15 млн. га земли, в том числе станичной надельной — 413 
  тысяч га (на одного казака 11,9 га). <br>
  <br>
  В начале ХХ века Терское казачье войско по штатам мирного времени выставляло 
  4 конных полка, 2 батареи, 2 гвардейских сотни и 10 команд (свыше 5 тысяч человек). 
  В Первую мировую войну Терское войско выставило 12 конных полков, 2 сотни Его 
  Императорского Величества конвоя, 3 конвойных полусотни, 2 пластунских батальона, 
  3 конных батареи, 2 запасных конных полка и запасную пешую сотню (всего — 18 
  тысяч человек). <br>
  <br>
  Революционные события в стране привели к созданию на территории Терского казачьего 
  войска новых государственных образований, многочисленных органов власти, стремившихся 
  повлиять на ход событий в этом многонациональном регионе. Полиэтнический состав 
  населения Терской области, наличие большого количества нерешенных вопросов, 
  уходивших корнями далеко в прошлое, обусловил особенно жестокий ход Гражданской 
  войны, имевшей на Северном Кавказе ярко выраженный межнациональный характер. 
  Конфликты, сдерживаемые ранее сильной центральной властью, с началом междоусобной 
  войны раздирали кавказские народы, создавая почву для утверждения Советской 
  власти в этом стратегически важном регионе, а внешние воздействия еще более 
  запутали внутриплеменную рознь. Народы Северного Кавказа, в большей или меньшей 
  степени попали под влияние Советской власти, оспариваемое Турцией, усиленно 
  привлекавшей с конца 1917 г. горцев пантюркистской и панисламистской агитацией<sup>1</sup>. 
  <br>
  <br>
  В начале декабря 1917 г. по инициативе терского Войскового атамана подъесаула 
  М.А. Караулова было создано Временное Терско-Дагестанское правительство, образованное 
  Терским казачьим правительством, Центральным комитетом Союза горцев Кавказа 
  и Союзом городов Терской и Дагестанской областей и принявшее на себя всю полноту 
  государственной власти. Однако его влияние в действительности было крайне ограниченно, 
  и игнорировалось многочисленными советами, существовавшими на территории области, 
  а после убийства 13(26) декабря 1917 г. на станции Прохладной атамана Караулова 
  упало еще более (тогда же, в декабре был убит ингушами товарищ (заместитель) 
  Войскового атамана — Л.Е. Медяник). <br>
  <br>
  Фактически, уже в начале 1918 г., власть в Терском крае перешла к местным советам 
  и возвращавшимся с Кавказского фронта бандам солдат, терроризировавших местное 
  население, насаждавших советы и поступавших к ним на службу. В конце января 
  1918 г. в Моздоке (затем в Пятигорске) состоялся Рабоче-крестьянский съезд, 
  на котором было образовано новое правительство области — Терский народный совет 
  во главе с Ю.Г. Пашковским. На протяжении месяца Народный совет правил параллельно 
  с Терско-Дагестанским правительством. 9 (21) марта, с самоустранением Терско-Дагестанского 
  правительства (в целях избежания кровопролития и ввиду отсутствия поддержки 
  оно сложило с себя полномочия и предложило Народному совету переехать во Владикавказ) 
  на Тереке установилась Советская власть, а Терская область была объявлена составной 
  частью Российской советской республики. Верховной властью края считался Терский 
  народный съезд, в который входили представители советов и всех социалистических 
  партий; органом управления и законодательства - избранный съездом Народный совет; 
  исполнительную власть осуществлял Совет народных комиссаров (СНК) во главе с 
  С.Г. Баучидзе (ветви власти имели соответственные подразделения в казачьих отделах 
  и горских округах). Независимо от них, в крае продолжали свою деятельность местные 
  советы и исполнительные комитеты рабочих и солдатских депутатов, органы самоуправления 
  терских казаков и социалистические городские думы, что усиливало безвластие 
  и анархию в крае<sup>2</sup>.<br>
  <br>
  Новая власть, как и в случае с другими казачьими войсками, старалась разрушить 
  военную структуру терского казачества, мешавшего осуществлению ее политики в 
  регионе. Терское казачество, несмотря на влияние большевицкой пропаганды крепко 
  держалось за свои привилегии, носившие главным образом экономический характер. 
  Конец 1917 — начало 1918 гг. ознаменован разложением терского казачества, расслоением 
  казаков и иногородних, их обособлением в пределах расселения народов Северного 
  Кавказа, что негативно повлияло на судьбы терцев и привело к разгоранию Гражданской 
  войны в регионе<sup>3</sup>. Одновременно с поддержкой горских национальных 
  органов власти краевой СНК ликвидировал остатки казачьего самоуправления и распустил 
  терские воинские части. Принятый на 3-м Терском народном съезде аграрный закон, 
  отнимал у терцев земли в пользу крестьян и отдавал часть казачьих земель в пользу 
  Чечни и Ингушетии, Эти меры привели к падению духа терского казачества: вопрос 
  касался уже не сохранения вольностей, а ликвидации войска. 7-й казачий съезд, 
  под давлением краевого СНК и казаков-большевиков постановил отказаться от всех 
  привилегий и вольностей терцев. <br>
  <br>
  Одновременно СНК начал репрессии по отношению к офицерству, многие тысячи которого 
  сосредоточились по всему Кавказу, и особенно в минераловодской группе. В апреле 
  1918 г., под давлением Москвы краевой СНК приступил к формированию Красной армии, 
  обратившись к офицерам с патриотическим воззванием: помочь родине предотвратить 
  германо-турецкое нашествие. В мае в Пятигорске состоялся офицерский митинг, 
  в котором приняли участие около тысячи человек, принявший резолюцию о необходимости 
  создания в крае Народной армии. Во главе ее был поставлен генерал Мадритов, 
  позднее отмечавший, что целью руководителей Народной армии было «сформировать 
  Терскую армию, совершить переворот и присоединиться к Добровольческой армии». 
  Однако, по словам А.И. Деникина, ни желания, ни смелости, ни малейшей работы 
  со стороны этой армии не наблюдалось<sup>4</sup>. <br>
  <br>
  После временного захвата отрядом партизан полковника А.Г. Шкуро Кисловодска, 
  в июне 1918 г. местные офицерские организации распались, начались репрессии. 
  «Офицерство беспомощно подставляло свои головы под удары большевицких палачей 
  или распылялось по станицам и аулам; очень немногие ушли со Шкуро и в последовавшей 
  летом и осенью 1918 г. вооруженной борьбе из двух, по крайней мере, десятков 
  тысяч офицеров приняли участие лишь отдельные лица, небольшой отряд в 300—500 
  человек полковника [Б.Л.] Литвинова и два-три более мелких». К середине 1918 
  г., когда преждевременно началось Терское восстание, единый белый центр на Северном 
  Кавказе в полной мере так и оформился. Однако политика и практика Советской 
  власти в регионе неизбежно должны были привести к восстанию наиболее угнетаемых 
  элементов, какими были русское офицерство и терское казачество<sup>5</sup>. 
  <br>
  <br>
  Подготовка к восстанию велась с конца мая 1918 г. в минераловодской группе, 
  в Моздоке и Владикавказе. Выступление было намечено на август, однако захват 
  полковником Шкуро в середине июня Кисловодска спровоцировал преждевременное 
  восстание Волгского отдела терцев, перекинувшееся на Моздокский отдел. В ходе 
  восстания было создано несколько линий, представлявших собой фронты, во главе 
  с начальниками: Прохладненская (была основной, удерживала железную дорогу Новопавловская 
  — Нальчик), Курская, Кизлярская, Грозненская, Владикавказская, Сунженская, Котляревская 
  и Боргустанская. <br>
  <br>
  Военно-политическим центром восстания стал Моздок, куда переехала и казачья 
  фракция Народного совета из Владикавказа. Уже 20 июня (3 июля) в Моздоке состоялся 
  Съезд казаков и крестьян Терского войска, избравший в качестве исполнительного 
  органа Казаче-крестьянский совет во главе с социалистом-революционером Г.Ф. 
  Бичераховым. Совет стал во главе восстания, признав командующим генерал-майора 
  Э.А. Мистулова (вместо раненого полковника Н.К. Федюшкина). Политическая окраска 
  руководящего органа восстания наложила отпечаток на весь его ход. На протяжении 
  июля Совет вел безуспешные переговоры с краевым СНК, а затем с собравшимся во 
  Владикавказе 4-м Съездом трудовых народов. Признавая Советскую власть, Совет 
  требовал изменения политики СНК в отношении терских казаков, прекращения репрессий, 
  возвращения оружия и отставки наиболее запятнавших себя комиссаров. <br>
  <br>
  Переговоры были прерваны 25 июля (7 августа) восстанием во Владикавказе, поднятым 
  полковниками И.Н. Беликовым и С.А. Соколовым. Часть членов СНК была захвачена 
  восставшими, но затем отпущена. Оставшиеся депутаты Народного съезда, ввиду 
  ликвидации СНК, выделили из своего состава Исполнительный комитет, который возглавил 
  Терскую республику и владикавказское восстание, назначив командующим всеми вооруженными 
  силами республики генерала Мадритова. Однако ввиду неспособности вооруженных 
  частей вести боевые действия, Комитет и командующий армией оставили Владикавказ, 
  подвергнувшийся разграблению. Разогнанный во время восстания СНК с приходом 
  красных частей в город был восстановлен. Прибывший в Моздок Исполнительный комитет 
  при содействии Г.Ф. Бичерахова объявил себя Временным народным правительством 
  Терского края, подчинив своей власти Казаче-крестьянский совет и взяв на себя 
  руководство восстанием. В середине сентября в Моздоке был созван новый Казаче-крестьянский 
  съезд Терского края, призванный решить принципиальные вопросы устройства построения 
  власти<sup>6</sup>. <br>
  <br>
  Связь восставших с Добровольческой армией установилась только в сентябре 1918 
  г., когда 9 (22) сентября 1918 г. для связи с терцами, в распоряжение генерала 
  Д.Ф. Левшина, был послан генерал И.Н. Колесников с небольшой суммой денег. Последнему 
  командованием Добровольческой армии предписывалось добиваться установления на 
  Тереке единоличной атаманской власти. В результате на Тереке образовались два 
  центра руководства восстанием: первый в Прохладной, во главе с командующим войсками 
  полковником Н.К. Федюшкиным, бывшим председателем Войскового круга П.Д. Губаревым 
  и Д.Ф. Левшиным, второй — в Моздоке, во главе с Г.Ф. Бичераховым и эсеровским 
  комитетом. В итоге начинания терского командования, призванные укрепить фронт 
  и повысить боеспособность армии разбивались о противодействие моздокского правительства. 
  Результатом противостояния двух центров стала организационная незавершенность 
  структуры терской армии: на фронте бились отдельные отряды ополченского характера, 
  восставшие служили посменно, зачастую обнажая фронт, испытывая недостаток в 
  боеприпасах. Под влиянием этих условий и при постоянном воздействии большевицкой 
  пропаганды дух терцев падал. Некоторые станицы целиком или частично переходили 
  на сторону большевиков. «Достойно удивления, как при этих условиях — без дисциплины, 
  без денег, без боевых припасов, почти в полном окружении — в течение пяти месяцев 
  командный состав и лучшая часть казачества находили силы продолжать борьбу. 
  Дрались и умирали, не теряя веры в свое дело и конечный его успех»<sup>7</sup>. 
  Ведя бои с переменным успехом, отряды терцев постоянно меняли свой состав, доходивший 
  в среднем до 12 тысяч человек при 40 орудиях. К осени 1918 г. наиболее боеспособные 
  отряды терцев занимали следующее расположение: станица 3ольская в переходе от 
  Пятигорска (полковник В.К. Агоев), позиции в переходе к югу-востоку от Георгиевска 
  (полковник Г.А. Вдовенко). Отдельные терские отряды прикрывали войско с севера 
  у Курской и с юга от Владикавказа — у Котляревской. Бои шли у Грозного и Кизляра 
  (города переходили из рук в руки), против Владикавказа стояли осетинские сотни 
  полковника Я. Хабаева, Большую Кабарду и Нальчик занимали осетинские и кабардинские 
  отряды Г.А. Кибирова и 3. Даутокова-Серебрякова («Отряд Свободы»)<sup>8</sup>. 
  <br>
  <br>
  Под давлением наступавшей с запада Добровольческой армии большевики вынуждены 
  были пробивать себе путь на восток через Моздок и Кизляр, предприняв в начале 
  ноября серьезное наступление в этом направлении двумя колоннами: от Георгиевска 
  на Моздок и от Пятигорска на Прохладную. Терские казаки вынуждены были отступать, 
  не оказывая серьезного наступления. Из-за отсутствия подкреплений, отвлеченных 
  к Моздоку, вскоре был оставлен Грозный, а затем снята осада Кизляра. На место 
  смещенного терским правительством 4(17) октября командующего Казачье-крестьянской 
  армией полковника Н.К. Федюшкина был назначен до конца не оправившийся от ранения 
  генерал Э.А. Мистулов. На совместном заседании Временного правительства и Казаче-крестьянского 
  съезда было решено объявить в крае всеобщую мобилизацию, однако мера оказалась 
  явно запоздалой и не повлияла на ход боевых действий: сказывалось отсутствие 
  боеприпасов, деморализация и самовольное оставление казаками позиций. Усугубил 
  положение временный выход из строя наиболее авторитетных казачьих воинских начальников: 
  полковников В.К. Агоева (ранен) и Г.А. Вдовенко (заболел). Под влиянием этих 
  обстоятельств, непрекращающихся ссор и интриг в правительстве и Совете 28 октября 
  (10 ноября) 1918 г. покончил жизнь самоубийством генерал Э.А. Мистулов<sup>9</sup>.<br>
  <br>
  Терский фронт прекратил существование. Перед тем как рассеяться, правительство 
  передало власть триумвирату, в который вошли Г.Ф. Бичерахов, Букановский и новый 
  командующий войсками генерал-майор И.Н. Колесников. Вскоре вся территория Терского 
  войска вновь оказалась под контролем большевиков. Остатки войск восставших рассеялись 
  по Северному Кавказу: около двух тысяч терцев, во главе с новым командующим 
  и правительством направилась к Петровску; отряды полковников Б.Н. Литвинова, 
  В.К. Агоева, 3. Даутокова-Серебрякова и Г.А. Кибирова (общей численностью около 
  4 тысяч человек), вышли через горы южнее Баталпашинска на соединение с Добровольческой 
  армией (кроме этих отрядов еще в середине июля Добровольческую армию пополнил 
  кадр распущенного 1-го Волгского полка, прибывший в составе Конной дивизии Отряда 
  полковника Шкуро); часть казаков во главе с полковником Я. Хабаевым пробилась 
  к Военно-Осетинской дороге<sup>10</sup>. <br>
  <br>
  С окончанием Северокавказской операции Добровольческой армии Вооруженные силы 
  на Юге России установили контроль над большей частью территории Северного Кавказа. 
  10 (23) января 1919 г. А.И. Деникин назначил главноначальствующим и командующим 
  войсками созданного Терско-Дагестанского края командира III-го армейского корпуса 
  генерала В.П. Ляхова, которому, в целях воссоздания Терского казачьего войска 
  было приказано собрать казачий Круг для выбора Войскового атамана. Терский Большой 
  Войсковой Круг открылся 22 февраля (7 марта). Он высказал поддержку Добровольческой 
  армии, избрал Малый Круг (Комиссию законодательных положений), утвердил временную 
  конституцию края и избрал атаманом генерал-майора Г.А. Вдовенко, получившего 
  полную военную и исполнительную власть. <br>
  <br>
  Ко времени занятия Добровольческой армией Северного Кавказа из самостоятельных 
  терских частей, уцелевших после поражения восстания, сохранился только отряд 
  терских казаков в Петровске во главе с командующим войсками Терского края генерал-майором 
  И.Н. Колесниковым. В его состав входили Гребенский и Горско-Моздокский конные 
  полки, Конная сотня копайских казаков, 1-й Моздокский и 2-й Гребенский пластунские 
  батальоны, Пешая сотня копайских казаков, 1-й и 2-й артиллерийские дивизионы. 
  К 14(27) февраля 1918 г. он насчитывал 2088 человек.<sup>11</sup><br>
  <br>
  Одной из первых частей терцев, присоединившейся к Добровольческой армии, был 
  Терский офицерский полк, сформированный 1(14) ноября 1918 г. из офицерского 
  отряда полковника Б.Н. Литвинова, прибывшего в армию после поражения Терского 
  восстания (расформирован в марте 1919 г.), а также отряды полковников В.К. Агоева, 
  3. Даутокова-Серебрякова и Г.А. Кибирова. 8(21) ноября в составе Добровольческой 
  армии был сформирован 1 й Терский казачий полк (позднее влитый в состав 1-й 
  Терской казачьей дивизии). Широкое формирование терских частей началось с утверждением 
  Добровольческой армии на Северном Кавказе. Основу терских формирований в Гражданскую 
  войну составляли 1-я, 2-я, 3-я и 4-я Терские казачьи дивизии и 1-я, 2-я, 3-я 
  и 4-я Терские пластунские бригады, а также Терские казачьи конно-артиллерийские 
  дивизионы и отдельные батареи, входившие как в состав Войск Терско-Дагестанского 
  края, так и Добровольческой и Кавказской Добровольческой армий. Начиная с февраля 
  месяца, терские формирования уже вели самостоятельные боевые операции против 
  большевиков и горцев. Это было особенно значимо для белых сил на юге, в связи 
  с переброской Кавказской Добровольческой армии на северный фронт. <br>
  <br>
  Терская пластунская отдельная бригада была сформирована в составе Добровольческой 
  армии 9 (22) декабря 1918 г. из вновь сформированных 1-го и 2-го Терских пластунских 
  батальонов и Терского казачьего артиллерийского дивизиона, в состав которого 
  входили 1-я Терская казачья и 2-я Терская пластунская батареи. На ее основе 
  была в январе 1919 г. была сформирована 1-я Терская пластунская отдельная бригада, 
  входившая в состав III-го армейского корпуса Кавказской Добровольческой армии, 
  а затем Войск Северного Кавказа. Она первоначально включала в свой состав 1-й, 
  2-й, 3-й и 4-й Терские пластунские батальоны, Боргустанскую конную сотню и Терскую 
  пластунскую батарею<sup>12</sup>; 10 (23) апреля в ее состав была включена 1-я 
  Терская легкая гаубичная батарея. К 5 (18) октября бригада насчитывала 819 штыков 
  в составе 1-го и 2-го Волгских, 1-го и 2-го Сунженско-Владикавказских пластунских 
  батальонов и 1-го Кавказского артиллерийского дивизиона, а также 9-го Терского 
  пластунского батальона (позднее был влит в состав 1-го Сунженско-Владикавказского)<sup>13</sup>. 
  <br>
  <br>
  В феврале 1919 г. была сформирована 2-я Терская пластунская отдельная бригада, 
  включавшая в свой состав 5-й, 6-й, 7-й и 8-й Терские пластунские батальоны. 
  Первоначально она входила в состав Кавказской армии, к середине июля 1919 г. 
  — в группу генерала Д.Н. Промтова (вошла с ней состав Добровольческой армии), 
  а затем в состав Войск Киевской области. Участвовала в Бредовском походе. В 
  1919 г. в состав бригады входили 1-й и 2-й Горско-Моздокские и 1-й и 2-й Кизляро-Гребенские 
  пластунские батальоны и 2-й Кавказский пластунский артиллерийский дивизион (с 
  29 июля 2-я Кавказская пластунская легкая гаубичная и 2-я Кавказская пластунская 
  горная батарея). К 20 сентября (3 октября) бригада насчитывала 1504 штыка и 
  включала 5-й, 6-й, 7-й, 8-й, 13-й и 15-й Терские пластунские батальоны, запасную 
  сотню и 2-ю Терскую гаубичную батарею. 16-й Терский пластунский батальон и 2-я 
  Кавказская пластунская батарея на 5 (18) октября находились в составе Войск 
  Северного Кавказа<sup>14</sup>. 2 (15) января 1920 г. бригада была временно 
  расформирована командующим Войсками Киевской группы генерал-лейтенантом Н.Э. 
  Бредовым и сведена в Отдельный сводный Терский батальон<sup>15</sup>. <br>
  <br>
  Весной 1919 г. были также сформированы 3-я и 4-я Терские пластунские отдельные 
  бригады. В состав 3-й бригады входили 9-й, 10-й, 11-й и 12-й Терские батальоны, 
  в состав 4-й — 13-й, 14-й, 15-й и 16-й. Однако уже 26 июля (8 августа) 3-я бригада 
  была расформирована, а ее батальоны, сведенные в 1-й и 2-й Сунженско-Владикавказские 
  батальоны были влиты в состав 1-й Терской пластунской отдельной бригады. 4-я 
  Терская пластунская бригада также была расформирована и к осени 1919 г. влита 
  в состав 2-й бригады. <br>
  <br>
  1-я и 2-я Терские пластунские бригады были расформированы 6 (19) мая 1920 г. 
  После расформирования из остатков 2-й бригады был сформирован Терский пластунский 
  полк, вошедший в состав 7-й пехотной дивизии III-го армейского, а с октября 
  1920 г. — Кубанского корпусов. <br>
  <br>
  1-я Терская казачья дивизия была сформирована в составе Кавказской Добровольческой 
  армии в конце 1918 — начале 1919 г. на базе Сводной Терско-Кубанской казачьей 
  бригады как Терская казачья конная дивизия. В ее состав в входили 1-й, 2-й и 
  3-й Волжские и 1-й Терский казачьи конные полки, Терская конно-горная и Волгская 
  батареи, а позднее Стрелковый полк<sup>16</sup>. В 1919 г. дивизия входила в 
  состав 3-го Кубанского корпуса Добровольческой армии. 2 (15) сентября 1919 г. 
  была переформирована в бригаду и включена в состав вновь созданной 1-й Терской 
  казачьей дивизии. <br>
  <br>
  25 января (7 февраля) 1919 г. была сформирована 2-я Терская казачья дивизия, 
  первоначально входившая в состав Грозненской группы Войск Северного Кавказа, 
  а с 13 (26) апреля — в 1-й Кубанский корпус Кавказской армии. В составе дивизии 
  были 2-й и 3-й Горско-Моздокские и 2-й Терский казачьи полки. 2 (15) сентября 
  дивизия была переформирована в бригаду и включена в состав вновь созданной 1-й 
  Терской казачьей дивизии. <br>
  <br>
  К 23 февраля (8 марта) 1919 г. были сформированы 3-я и 4-я Терские казачьи дивизии, 
  также вошедшие в состав Войск Северного Кавказа. В состав 3-й дивизии вошли 
  1-й, 2-й и 3-й Сунженско-Владикавказский и 3-й Терский казачий полки, 4-я и 
  6-я Терские казачьи батареи, в состав 4- й — 1-й, 2-й и 3-й Кизляро-Гребенские, 
  4-й Терский казачий полки и 5-я Терская казачья батарея. К 5 (18) октября численность 
  3-й дивизии составляла 1145 сабель, 4— 1056 сабель. 18 ноября (1 декабря) 1919 
  г. из дивизий была сформирована новая 2-я Терская казачья дивизия<sup>17</sup>. 
  <br>
  <br>
  В состав новообразованной 1- й дивизии, вошедшей в состав 3-го Кубанского корпуса 
  Добровольческой армии (в октябре был временно придан Донской армии), входили 
  1-й и 2-й Волгские, 1-й и 2-й Горско-Моздокские полки, 1-й Терский казачий конно-артиллерийский 
  дивизион и Стрелковый полк. В состав 2-й дивизии, первоначально входившей в 
  состав Войск Северного Кавказа, а затем 1-го Кубанского корпуса Кавказской армии, 
  вошли 1-й и 2- й Сунженско-Владикавказские, 1-й и 2-й Кизляро-Гребенские полки 
  и 2-й Терский казачий конно-артиллерийский конный дивизион, 6-я батарея и запасная 
  Терская Кавказская конно-артиллерийская батарея. <br>
  <br>
  Конструкция власти, образовавшаяся в начале 1919 г. в Терско-Дагестанском крае 
  привела к соуправлению краем двух центров: казачьего и добровольческого (оба 
  находились в Пятигорске). Как отмечал позднее А.И. Деникин, нерешенность ряда 
  вопросов, восходивших еще к дореволюционному времени, недоговоренность во взаимоотношениях, 
  влияние на терцев кубанских самостийников, не могли не породить трений между 
  этими двумя властями. Только благодаря осознанию смертельной опасности в случае 
  разрыва, отсутствию в массе терского казачества самостийных тенденций, личным 
  взаимоотношениям представителей обеих ветвей власти, государственный механизм 
  на Северном Кавказе работал на протяжении 1919 г. без существенных перебоев. 
  До конца белой власти край продолжал находиться в двойном подчинении: представитель 
  добровольческой власти (генерала Ляхова 16 (29) апреля 1919 г. сменил генерал 
  от кавалерии И.Г. Эрдели) руководствовался «Основными положениями» о Терско-Дагестанском 
  крае, составление которых было закончено Особым совещанием в мае 1919 г.; Войсковой 
  атаман правил на основании Терской конституции. В результате двоевластия трения 
  между добровольческими и терскими властями не прекращались на протяжении всего 
  1919 г. «Но никогда эти трения не переходили границ. Атаман был всегда лоялен 
  и никогда не принимал участия в выступлениях против власти юга». Политические 
  разногласия и недоразумения между представителями двух властей, как правило, 
  заканчивались принятием компромиссного решения. Трения между двумя центрами 
  власти на протяжении 1919 г. создавала главным образом небольшая, но влиятельная 
  часть радикальной самостийной терской интеллигенции в правительстве и Круге. 
  Наиболее наглядной иллюстрацией может служить позиция терской фракции Верховного 
  казачьего Круга, собравшегося в Екатеринодаре 5 (18) января 1920 г. в качестве 
  верховной власти Дона, Кубани и Терека. Терская фракция сохранила лояльное отношение 
  к правительству Юга России, исходя из положения неприемлемости для войска сепаратизма 
  и судьбоносности горского вопроса. Резолюция о разрыве отношений с Деникиным 
  была принята Верховным Кругом Дона, Кубани и Терека при незначительном количестве 
  голосов терской фракции, большая часть которой разъехалась по домам<sup>18</sup>. 
  <br>
  <br>
  На протяжении 1919 г., ввиду непрекращающейся борьбы с чеченцами и ингушами, 
  терская власть была озабочена численным увеличением казачества и привлечением 
  в войско союзников из числа соседей. В состав войска были включены каранагайцы, 
  а после продолжительных переговоров, при участии представителей главноначальствующего 
  Терско-Дагестанского края, Терский Круг третьего созыва выразил принципиальное 
  согласие на присоединение к Войску на равных правах осетин и кабардинцев (окончательное 
  решение не было принято из-за разразившейся вскоре на юге катастрофы). Между 
  тем, внутренняя борьба в Терско-Дагестанском крае продолжалась на протяжении 
  всего 1919 г., вынуждая командование ВСЮР оттягивать в Край дополнительные силы. 
  Почва для неудовольствия горских народов существующей властью была подготовлена 
  разными причинами: тяжелым экономическим положением, острой враждой между терцами 
  и горцами, темнотой масс, несправедливостью и поборами местной туземной администрации. 
  Немалое влияние на разрастание конфликтов оказывали горские правительства и 
  организации, обосновавшиеся в Закавказье<sup>19</sup>. <br>
  <br>
  Поражение «Похода на Москву», отступление ВСЮР зимой 1919—1920 гг. вызвало подъем 
  в крае деятельности национальных горских формирований, поддерживавших большевиков. 
  Эвакуация ВСЮР в Крым в марте 1920 г. послужила сигналом к ликвидации Терского 
  края. К середине марта 1920 г. красные повстанцы заняли Минеральные Воды, Пятигорск 
  и Геленджик. В конце марта ими без боя был взят Майкоп, а 11-я красная армия 
  и местные формирования заняли весь Дагестан. 23 марта (5 апреля) 1920 г. красные 
  партизаны вместе с грозненскими красногвардейцами вошли во Владикавказ. К началу 
  апреля Северный Кавказ находился под контролем Советской власти. С ее установлением 
  получила продолжение одна из самых мрачных страниц в истории Северного Кавказа 
  — переселение терского казачества, начатое еще в 1918 г. <br>
  <br>
  После эвакуации ВСЮР в Крым из остатков терских и астраханских казачьих частей 
  в апреле 1920 г. была сформирована Отдельная Терско-Астраханская казачья бригада, 
  которая, с 28 апреля (11 мая) как Терско-Астраханская бригада входила в состав 
  3-й конной дивизии Сводного корпуса. 7 (20) июля, после переформирования бригада 
  вновь стала отдельной. Летом 1920 г. входила в состав Группы войск особого назначения, 
  участвовавшей в Кубанском десанте. С 4 (17) сентября бригада действовала отдельно 
  в составе Русской армии и включала 1-й Терский, 1-й и 2-й Астраханские полки 
  и Терско-Астраханский казачий конно-артиллерийский дивизион и Отдельную Терскую 
  запасную казачью сотню. <br>
  <br>
  После эвакуации Русской армии из Крыма в Чаталдже в составе Донского корпуса 
  был сформирован Терско-Астраханский полк. После преобразования армии в РОВС 
  полк до 1930-х гг. представлял собой кадрированную часть (осенью 1925 г. насчитывал 
  427 человек, в том числе 211 офицеров). В эмиграции Терское казачье войско возглавляли 
  генерал-лейтенант Г.А. Вдовенко (до 1945 г.), генерал-майор К.К. Агоев (1953-1971), 
  хорунжий Н.Н. Протопопов(1973-1998). <br>
