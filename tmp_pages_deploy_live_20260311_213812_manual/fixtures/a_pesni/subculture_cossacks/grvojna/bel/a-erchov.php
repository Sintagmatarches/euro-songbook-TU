        <strong>Барабаш Яков Яковлевич</strong> (28.06.1880-1920). Сын Наказного 
        атамана Оренбургского казачьего войска (Далее — ОКВ). Окончил 4 класса 
        кадетского корпуса и 5 классов частного реального училища. На службе с 
        09.10.1898 г. Хорунжий (с 06.04.1903 со старшинством с 05.09.1902). Сотник 
        (1917 со старшинством с 16.11.1913). В 1904 командирован во 2 Аргунский 
        полк Забайкальского казачьего войска. В 1917 г. сотник 16 Оренбургской 
        казачьей сотни. В январе 1917 г. переведён в 2 запасную казачью сотню. 
        Осуждён к расстрелу ОО ВЧК при Совтрударме 30.04.1920. Реабилитирован 
        26.05.1992. <br>
        <br>
        <strong>Белов Иван Иванович</strong> (1864-?). Генерал-майор. Из казаков 
        Бердской станицы. Окончил Оренбургскую гражданскую гимназию, Оренбургское 
        казачье юнкерское училище по 2 разряду, Офицерские стрелковую и кавалерийскую 
        школы. На службе с 01.08.1883. Хорунжий (с 15.03.1888). Есаул (с 27.03.1906 
        со старшинством с 06.05.1901). Войсковой старшина (с 1916, со старшинством 
        с 19.11.1915). Полковник (с 10.1917 со старшинством с 19.03.1917). Участвовал 
        в русско-японской войне. Во 2 Оренбургском казачьем полку с 10.01.1914. 
        Командир 2 сотни с 29.01.1914. Заведующий хозяйством полка с 22.02.1916. 
        Награжден орденами: Св. Станислава. 2 ст. с мечами (1906), Св. Владимира 
        4 ст. с мечами и бантом (1904), Св. Анны 4 ст. «За храбрость», Св. Анны 
        2 ст. с мечами, Св. Станислава 3 ст. с мечами и бантом, Георгиевским оружием 
        за бой 06.09.1915. 1 июля 1916 помощник командира полка. В Гражданскую 
        войну — генерал-майор. <br>
        <br>
        <strong>Дашкин Зюлькарнаин Шангиреевич</strong> (1861-?), генерал-майор, 
        командир 14 Оренбургского казачьего полка. Из дворян ОКВ. Окончил Оренбургское 
        казачье юнкерское училище и Офицерскую кавалерийскую школу. На службе 
        с 1876, хорунжий (с 1881). Службу походил в 5 и 4 Оренбургских казачьих 
        полках в Туркестанском военном округе. В Первую мировую войну, войсковой 
        старшина, командир 14 Оренбургского казачьего полка. За боевые отличия 
        награждён орденами: Св. Анны 2-й ст. с мечами и Св. Владимира 4-й ст. 
        с мечами и бантом и чином полковника. В конце 1917 г. ему поручено формирование 
        частей из мусульман ОКВ. В феврале 1918 г. съезд трудового казачества 
        поручил ему заведование Войсковым штабом «впредь до избрания Советской 
        Власти». В 1918 г. входил в состав Башкирского правительства. <br>
        <br>
        <strong>Донецков Александр Петрович</strong> (1888-?) — войсковой старшина 
        ОКВ. Командир 1-го Линейного пока. Из войскового сословия. Окончил Оренбургское 
        казачье военное училище по 1 разряду. На службе с 1911. Хорунжий (с 1914). 
        Младший офицер 13 Оренбургского казачьего полка с 19.07.1914. С 13.07.1917 
        по 18.01.1918 гг. командир сотни 13 Оренбургского казачьего полка. За 
        боевые отличия на фронтах Первой мировой войны награждён шестью боевыми 
        орденами и дважды повышался в чине. С 17.03.1918 по 15.07.1918 командир 
        Южного отряда Линейных станиц. В октябре 1918 г. произведён в войсковые 
        старшины. С остатками ! Линейного полка 12.01.1919 вошёл в состав 2 Оренбургской 
        казачьей дивизии. <br>
        <br>
        <strong>Милеев Андрей Петрович</strong> (1860-?), полковник, командир 
        13 Оренбургского казачьего полка. Из казаков станицы Оренбургской. Окончил 
        Оренбургское уездное училище и Оренбургское казачье юнкерское училище. 
        На службе 1878, хорунжий с 1881, есаул в 1901. В Первую мировую войну 
        войсковой старшина, командир 13 Оренбургского казачьего полка. За отличия 
        в боях награждён орденом Св. Владимира 3 ст. с мечами, чинами войскового 
        старшины и полковника. <br>
        <br>
        <strong>Смолин Николай Павлович</strong> (1862-?), полковник, командир 
        17 Оренбургского казачьего полка. Из казаков станицы Челябинской. Окончил 
        Оренбургскую военную прогимназию и Оренбургское казачье юнкерское училище. 
        На службе с 1879, хорунжий (с 1882). В Первую мировую войну командир 17 
        Оренбургского казачьего полка. За боевые отличия награждён орденами Св. 
        Владимира 4-й ст. с мечами и бантом и Св. Владимира 3-й ст. с мечами. 
