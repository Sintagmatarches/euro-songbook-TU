	<span class="text"><strong>В.Г. Семенов </strong><br>
        <br>
       <span class="header"><strong>ПРИЗВАНИЕ – ЗАЩИЩАТЬ ЗЕМЛЮ И НАРОД 
        (СУДЬБА ВОЙСКОВОГО СТАРШИНЫ Е. В. ЕРШОВА)<br>
        </strong><br>
        </span> <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2205, стр. 208-211</em>.<br>
        <br>
        <br>
        Через десять лет после начала Гражданской войны, весной 1928 г. в Оренбурге, 
        было начато рассмотрение дела об убийстве С.М. Цвиллинга, а также о саботаже 
        Советской власти и колхозного строительства жителями поселка Изобильного. 
        Среди обвиняемых оказался и бывший войсковой старшина Оренбургского казачьего 
        войска Е.В. Ершов, который на первом же допросе, состоявшемся 8 мая 1928 
        г. в графе политические убеждения указал: призвание «военное, защищать 
        землю и народ от других стран».<sup>1</sup><br>
        <br>
        Призвание защитника отечества было смыслом жизни для большинства казаков, 
        таковым оно было и для Е.В. Ершова. Евгений Васильевич Ершов родился в 
        казачьей семье в станице Изобильной в 1890 г.. В 1912 г. он окончил полный 
        курс Ташкентской гимназии, а в 1914 г. — Оренбургское казачье военное 
        училище. <br>
        <br>
        На второй день войны, 19 июля 1914 г., он начал службу в 13 Оренбургском 
        казачьем полку, и уже через 11 дней вместе с товарищами покинул Оренбург 
        и отправился на фронт.<sup>2</sup> Молодому хорунжему и его подчиненным, 
        большинство из которых были на 6-8 лет старше своего командира, пришлось 
        участвовать вместе с полком во многих тяжелых боях. В составе 4-й армии 
        Юго-Западного фронта в Галицийском сражении участвовали третьеочередные 
        13, 14, 16 и 17 Оренбургские казачьи полки. В составе Гренадерского корпуса 
        генерала И.И. Мрозовского в августовских боях участвовали 13 и 14 Оренбургские 
        казачьи полки. Первым из полков третьей очереди на театр военных действий 
        15 августа прибыл 13 Оренбургский казачий полк под командованием полковника 
        А. Милеева. Уже через два дня полк был придан к 3 Донской казачьей дивизии, 
        в этот же день оренбургские казаки участвовали в бою у деревни Ольшанка 
        Люблинской губернии. 20 августа 13 полк участвует в бою у деревень Суходол 
        и Травники. В ходе боя казаки преследовали разбитого пехотой противника, 
        только в плен взяли 150 австрийских солдат.<sup>3</sup> В ходе этого боя 
        в составе Гренадерского корпуса участвовал и 14 Оренбургский казачий полк 
        под командованием войскового старшины 3. Дашкина, 22 августа 1914 г. в 
        деревню Лапеника вошли с боем казаки 2 и 3 сотен 13 полка. В ходе схватки 
        оренбуржцы зарубили 18 и захватили в плен 97 австрийских солдат. 23 августа 
        в бою у Тарнавки казаками взято в плен еще 40 неприятельских солдат.<sup>4</sup> 
        21 августа в состав 3 Донской казачьей дивизии вошёл и 17 Оренбургский 
        казачий полк под командованием полковника Н. Смолина. Ситуация на фронте 
        4-й армии к этому времени сложилась следующая. К 26 августа 1914 г. 1-я 
        австро-венгерская армия генерала В. Данкеля и группа генерала Г. Куммера, 
        после ряда неудачных для них боёв, в районе южнее железной дороги Люблин-Холм, 
        приостановили своё отступление и закрепились на новых позициях. На поддержку 
        1 армии подошёл германский ландверный корпус генерала Р. Войрша, в составе 
        3 и 4 пехотных дивизий. 26 августа 4-я русская армия генерала от инфантерии 
        А.Е. Эверта повела фронтальные атаки с целью прорыва австро-венгерской 
        позиции. В стыке между 4-й и 5-й русскими армиями находилась 3 Донская 
        казачья дивизия, на которую была возложена задача: содействовать наступлению 
        4-й армии и поддерживать связь с 5-й армией. В это время 3 Донская казачья 
        дивизия была донской скорее по названию, чем по составу, так как состояла 
        из пяти полков: трёх Донских (18, 30 и 32) и двух оренбургских (13 и 17), 
        да и старшим адъютантом штаба (а позднее начальником штаба дивизии) был 
        оренбургский казак, - Генерального штаба подъесаул И.Г. Акулинин. Оренбургские 
        казачьи полки были включены в состав дивизии временно, в период боёв под 
        Люблином (20 Донской казачий полк, входивший в состав дивизии, находился 
        во временной командировке при XVI армейском корпусе). Что показывает определённые 
        недостатки или даже неразбериху в использовании отдельных кавалерийских 
        частей и соединений. 26 августа 17 Оренбургский казачий полк принял участие 
        в лихой кавалерийской атаке у деревни Драганы. И.Г. Акулинин в 1938 г. 
        в журнале «Часовой» описал этот бой, так как он сам по собственной инициативе 
        повёл казачью бригаду в атаку. «Перед началом атаки каждый оренбуржец 
        истово перекрестился и поплевал в правую ладонь, чтобы крепче держать 
        клинок. Местность к западу от д. Тарновки была открытая, ровная и казачьи 
        лавы доскакали до передовых окопов, занятых австрийской пехотой. Атака 
        казачьей бригады была так неожиданно и столь стремительна, что австрийцы 
        растерялись и, после беспорядочной стрельбы, побросали окопы. Многие пехотинцы, 
        спасаясь от казачьих пик и шашек, кинулись в воду. В ходе боя казаки захватили 
        около 300 пленных, (наши потери были менее чем в 10 раз). Однако атака 
        18 Донского и 17 Оренбургского казачьих полков не была поддержана ни тремя 
        остальными полками 3 Донской дивизии, ни соседними пехотными частями. 
        (13 Оренбургский казачий полк был брошен на поддержку 17 Оренбургскому 
        казачьему полку, но наткнулся на болото и был возвращён на исходные позиции). 
        Если бы это произошло, результаты атаки могли бы стать более впечатляющими. 
        30 августа казаки 13 Оренбургского казачьего полка участвовали в ночном 
        набеге на город Янов, в ходе которого были взяты богатые трофеи и сотни 
        пленных. После окончания Галицийского сражения командованию Северо-западного 
        и Юго-Западного фронтов верховный главнокомандующий русской армии ставит 
        задачу «переход в наступление возможно большими силами от Средней Вислы 
        в направлении к Верхнему Озеру для глубокого вторжения в Германию».<sup>5</sup> 
        Эта операция вошла в историю как Варшавско-Ивангородское сражение, длившееся 
        с 15 сентября по 26 октября 1914 г. В операции участвовали 2-я, З-я, 4-я 
        и 5-я русские армии, около половины всех русских сил, действовавших против 
        Австро-Венгрии (всего 42 пехотных дивизии, 470 тысяч человек, 11 кавалерийских 
        дивизий, 50 тысяч сабель и 2400 орудий). Австро-германцы имели 9-ю германскую 
        и 1-ю австро-венгерскую армии, насчитывавшие 280 тысяч солдат, 20 тысяч 
        сабель и 1600 орудий. <br>
        <br>
        В составе Гренадерского корпуса 4-й армии 13 и 14 Оренбургские казачьи 
        полки в этом сражении выполняли роль корпусной конницы. С 19 сентября 
        оба полка производили разведку на левом берегу р. Вислы и охраняли переправы. 
        В период боёв на р. Висле 10 октября полки оренбуржцев переправились через 
        р. Вислу и прикрывали переправу Гренадерского корпуса, 11-15 октября прикрывали 
        левый фланг своего корпуса и поддерживали связь с 16 корпусом. При отступлении 
        австро-германцев к г. Ченстохову производили разведку и участвовали в 
        мелких стычках. 14 полк участвовал вместе с казаками Забайкальской казачьей 
        бригады в бою у деревень Домбровка и Грабовка, в ходе которого казаки 
        в пешем строю пошли в наступление, задержали наступление противника до 
        подхода Гренадерского корпуса. <br>
        <br>
        Среди казаков отличившихся в этих боях можно отметить казака 3 сотни 13 
        полка И. Липова, который в бою 12 октября 1914 г. в деревне Ясенчик отдал 
        поручику 1 Гренадерского Екатеринославского полка Нарышкину свою лошадь, 
        чтобы доставить донесение о положении дел в штаб 1 Гренадерской дивизии, 
        а сам ободрял и устраивал отступающих солдат у села Домбровки, говоря, 
        что он казак, а будет драться как пехотинец и, остановивши их, заставив 
        окопаться. <br>
        <br>
        20 октября 1914 г. его однополчанин урядник 6 сотни Е. Подтихов будучи 
        послан с разъездом из д. Будзислав на д. Олешко, заметил на мосту через 
        р. Чарна у д. Заброды привязанные противником к устоям моста пироксилиновые 
        шашки с горящим фитилём. Подвергаясь опасности, он срезал шашки и таким 
        образом предотвратил взрыв моста.<sup>6</sup> Наступательная операция 
        9 русских корпусов 4-й и 9-Й армий в ноябре (1-11) 1914 г. получила название 
        Ченстоховско-Краковской операции. В ходе встречных боёв русские войска 
        нанесли поражение австро-германцам и продвинулись на 30-50 км и сорвали 
        план австрийского командования окружить и разгромить русские армии. В 
        ходе сражения в составе Гренадерского корпуса 4-й армии участвуют казаки 
        13 и 14 Оренбургских казачьих полков. Казаки выполняли задачи по разведке 
        перед фронтом и на флангах корпуса, охраняли фланги и обеспечивали связь 
        между дивизиями и полками корпуса. В период отхода от Ченстохова к Конску 
        с 1 по 5 декабря 1914 г. прикрывали отход наших войск, с боями задерживая 
        противника и производя разведку, доставляя сведения о группировках сил 
        противника. В этих боях особо отличились казаки 13 Оренбургского казачьего 
        полка В. Яценков, П. Арьков, Л. Красноярцев. 8 ноября 1914 г., когда батальон 
        Аварского гренадерского полка оказался в окружении, они вызвались пробраться 
        через неприятельские укрепления, что успешно выполнили, чем способствовали 
        выходу их из окружения.<sup>7</sup><br>
        <br>
        За два года и три месяца боевых действий казаками 13 Оренбургского казачьего 
        полка только в плен было взято 5 офицеров и 832 солдата противника (почти 
        столько было строевых казаков в полку), в то время как казаки потеряли 
        убитыми всего 23 человека.<sup>8</sup><br>
        <br>
        В августе-сентябре 1916 г. начались выступления узбеков, киргизов, туркменов 
        и казахов против трудовых мобилизаций. Особенно активными они были в Фергане 
        и Тургайской степи. В ряде мест они шли под лозунгом объявления «священной 
        войны» — газавата. В результате этого правительству пришлось направить 
        в Среднюю Азию регулярные части, в том числе и с фронта. Первые волнения 
        в казахской степи на границе Оренбургской губернии произошли ещё в марте-июле 
        1916 г., когда для предотвращения беспорядков в Соль-Илецк был направлен 
        взвод Оренбургских казаков с офицером.<sup>9</sup> 24 августа 1916 г. 
        из состава 2 сводной казачьей дивизии выбыла 3 Оренбургская казачья батарея 
        в Семипалатинск. 29 ноября 1916 г. 13 Оренбургский казачий полк также 
        был отозван из действующей армии (из состава Гренадерского корпуса) и 
        через два дня в полном составе по железной дороге направлен внутрь империи. 
        <br>
        <br>
        29 ноября 1916 г. командир Гренадёрского корпуса генерал-лейтенант Д.П. 
        Парский издаёт приказ №300, в котором прощается с 13 Оренбургским казачьим 
        полком. «Из состава корпуса выбывает 13 Оренбургский казачий полк, прибывший 
        в половине августа 1914 г., и сразу попав в бой под Тарнавками и Суходолом 
        в составе 3-й Донской казачьей дивизии, затем полк свыше двух лет нёс 
        с корпусом боевую работу переносил с ним все боевые радости и печали. 
        <br>
        <br>
        В настоящий период летнего отхода 1915 г., полк прикрывал отходы корпуса, 
        в полной мере выполнил тяжёлую и ответственную задачу, давая возможность 
        пехоте спокойно отходить, устраиваться на новых позициях и вновь грозно 

        встречать наступающего врага. За двухлетний период боевой и походной жизни 
        и работы, гренадёры и казаки сжились и братски сроднились друг с другом. 
        Расставаясь ныне [с] 13 Оренбургским казачьим полком, я от себя и от гренадёров 
        выражаю искреннее пожелание всем членам полка полного успеха и благополучия 
        в новом месте службы. Полковника Милеева и всех г.г. офицеров благодарю 
        от лица службы, постоянное от сердца стремление выполнить всякое порученное 
        дело наилучшим образом не щадя себя. Молодцам казакам за их постоянную 
        лихую молодецкую работу моё сердечное спасибо. Добрый путь, лихие станичники 
        и не поминайте нас лихом». <br>
        <br>
        О чем думал молодой подъесаул Ершов, отмеченный за боевые отличия на фронте 
        шестью боевыми наградами (тремя орденами Св. Анны, двумя орденами Св. 
        Станислава и орденом Св. Владимира 4 ст. с мечами и бантом) и дважды повышенный 
        в звании. Может быть, вспоминал лихие атаки под Травниками и Суходолом 
        или встречу с императором Николаем II, 21 декабря 1915 г. под Могилевом. 
        <br>
        <br>
        16 декабря полк через Оренбург прибыл в г. Челкар Тургайской области и 
        вступил в распоряжение начальника экспедиционного отряда генерал лейтенанта 
        Лаврентьева. Через пять дней походным порядком направлен в г. Иргиз.<sup>10</sup><br>
        <br>
        Казаки понимали, что бунты в тылу в то время, когда вражеские полчища 
        топчут родную землю опасны, и надеялись, что легко наведут порядок и возвратятся 
        в действующую армию, где сейчас каждый боевой полк на вес золота. За участие 
        в этом походе Евгений Ершов был отмечен чином есаула. <br>
        <br>
        Осенью 1917 г. 13-й полк прибыл в Оренбургскую губернию и был расквартирован 
        в станице Павловской. Время наступило тревожное, но особого разброда, 
        как это происходило в пехотных частях, не было. Об этом свидетельствуют 

        выборы в Учредительное собрание (проходившие в ноябре): в 13 полку из 
        803 казаков за большевиков отдали голоса только трое. В декабре 1917-январе 
        1918 гг. есаул Ершов был среди тех, кто поддержал Войскового атамана А.И. 
        Дутова. После поражения белых отрядов в январе 1918 г. Дутов дал распоряжение: 
        «офицерство свободно». Евгений Васильевич в 1928 году писал: «И я, пользуясь 
        этим распоряжением, выехал в свою станицу», т.е. в Изобильную.<br>
        <br>
        12-25 марта 1918 г. в Оренбурге состоялся 1-й губернский съезд Советов. 
        Многие из казаков, участвовавших в его работе, заявили о своем «нейтралитете», 
        несмотря на утверждение С.М. Цвиллинга: «Кто не с нами, тот против нас!» 
        19 марта состоялось собрание казаков станицы Изобильной по вопросу о войне 
        (Гражданской войне). Узнав о том, что в Изобильной казаки организовали 
        вооруженную дружину, 10 марта в станицу приехал председатель Соль-Илецкого 
        военно-революционного комитета П.А. Персиянов с шестнадцатью красноармейцами. 
        Он стал призывать казаков к порядку, т.е. к признанию Советской власти 
        и требовать сдачи оружия. На что казаки «ответили ему все враз»: «У нас 
        оружия нет никакого, ты как приехал, так и уезжай, пока целый». <br>
        <br>
        Негодование казаков вызвали найденные в санях под соломой гранаты, раздались 
        крики: «Вот, мол, какое угощение привезли!» Позже Е.В. Ершов писал в объяснении: 
        «И на том собрании произошло озлобление против вооруженного отряда и когда 
        он (т.е. Персиянов) выехал из станицы, то в вдогонку поскакал Донецков 
        с отрядом, а после и я». После уничтожения отряда Персиянова со стороны 
        красных было решено уничтожить виновников того побоища и был выслан отряд 
        Цвиллинга. «Наше казачество, старики, узнав о движении отряда, собрали 
        старейшин и офицерство на совещание, чтобы выяснить (т.е. решить), как 
        поступить, и на том совещании было решено противодействовать вооруженной 
        силой и назначили на командную должность офицеров: Донецкова начальником 
        отряда, а меня начальником конницы...». <br>
        <br>
        Отряд Цвиллинга насчитывал около 400 красногвардейцев, в основном молодых 
        железнодорожников из Оренбурга и горняков из Соль-Илецка, многие из которых 
        не имели никакого боевого опыта. По архивным сведениям, у Цвиллинга в 
        отряде имелось 2 орудия и 3 пулемета. 2 апреля, обстреляв Изобильную из 
        орудий, красные стали входить в станицу, здесь Цвиллинг совершил свою 
        роковую ошибку, проявив элементарную военную безграмотность — он не провел 
        разведку, и тем самым принес в жертву революции несколько сот молодых 
        жизней, как, впрочем, и свою. Когда красные достигли центра станицы, казаки 
        внезапно с флангов ударили по ним. Один из участников этого боя, казак, 
        позже написал: «И ударили бела пехота из-за ограды по пулеметам. Всех 
        угодили прислугу пулемета красных! И пошла в атаку кавалерия с флангов. 
        И на середину прямо на Цвиллинга. Тут же бежали стары и малы и бабы. И 
        убили Цывиллинга (так в документе — В.С.) со товарищами. Но которые ушли 
        ближе к железной дороге не спаслись, - снег был глубокий, а день был жаркий, 
        снег рухнул, человека не поднимал». <br>
        <br>
        Ершов в этом бою сыграл очень важную роль. Он с пятью казаками проскакал 
        между двух отрядов и штаба Цвиллинга к красным орудиям и, захватив их, 
        открыл огонь по красным, чем усилил среди них панику. <br>
        <br>
        «И на том кончился бой, ни одного пленного не было взято в плен...», — 
        писал Е. Ершов, — «...После боя я с двумя сотнями пошл на Мертвецовку, 
        каковую взяли и образовали фронт. За боевые отличия против отряда Цвиллинга 
        был жителями станиц на Войсковом круге представлен к Георгиевскому кресту 
        и чину полковника, но мне был дан чин Войскового старшины, а впоследствии 
        орден Почетного Легиона (от союзников — В.С.) и Георгиевское оружие». 
        <br>
        <br>
        В ходе Гражданской войны Ершов служил в войсках под командованием генералов 
        И.И. Белова и И.Г. Акулинина. Приказом последнего войсковой старшина Ершов 
        назначен начальником казачьего партизанского отряда, состоявшего из казаков, 
        не желавших подчиняться строгой дисциплине, а также командовал 8 Оренбургским 
        казачьим полком. Газета «Оренбургский казачий вестник» во многих своих 
        материалах упоминала о Ершове как герое казачества. В белой армии Ершов 
        находился до ноября 1919 г., пока не заболел тифом (у берега Каспия). 
        Он был привезен в госпиталь Оренбурга, по выздоровлении направлен в Москву 
        в ВЧК по приговору М.В. Фрунзе на расстрел, замененный 5 годами ссылки.<sup>11</sup><br>
        <br>
        В связи с окончанием Гражданской войны Е. В. Ершов попал под амнистию. 
        В 1921 г. прошел военно-политические курсы и по окончании их служил два 
        года в РККА, был аттестован на должность помощника командира кавалерийского 
        полка, а потом (в 1923 г.) получил книжку ополченца, что посчитал незаслуженным 
        оскорблением, так как был профессиональным военным и мог принести больше 
        пользы на военном поприще, которое было его призванием. <br>
        <br>
        В 1923-1924 гг. Евгений Васильевич работал в детском доме в Ирбите. Здесь 
        он женился на Александре Александровне Загурской - правнучке декабриста, 
        полковника А.Ф. фон-дер Бриггена (1792-1859), отличившегося в Бородинском 
        сражении. <br>
        <br>
        О дальнейшей судьбе Е.В. Ершова мне сообщил его сын Георгий: «Мать вместе 
        с отцом работали воспитателями в детском доме им. Парижской Коммуны в 
        Ирбите. Отец имел обыкновение носить крест на шее и дети это видели. Начальство 
        детдома неоднократно его просило снять крест, так как тогда проводилась 
        политика воинствующего атеизма. Отец крест не снял и его уволили». Вскоре 
        Ершов уехал к матери в Соль-Илецк, а затем в Зилаир (Башкирия), где работал 
        счетоводом в лесхозе. <br>
        <br>
        Весной 1928 г. его арестовали и привезли в Оренбург, где начинало раскручиваться 
        юбилейное (ровно через 10 лет) дело об убийстве Цвиллинга и саботаже Советской 
        власти и колхозного строительства жителями поселка Изобильного. По делу 
        проходило 20 человек (И.Т. Самарцев, М.Я. Зобнин, С.П. Красноярцев, И.И. 
        Клюев, В.П. Хотенов, П.В. Орлов, И.Ф. Вдовкин, Н.П. Баладурин и другие). 
        Первый допрос Ершова состоялся 8 мая 1928 г. В графе «политические убеждения» 
        записано: военное, защищать землю и народ от других стран. 12 мая состоялся 
        второй допрос, на котором Ершов заявил: «В предъявленном обвинении от 
        11 мая 1928 г. виновным себя признаю лишь как офицер, принимавший активное 
        участие в гражданских боях и получил за это награды, упомянутые в протоколе 
        допроса от 8 мая 1928 г. Я заявляю, что я являлся исполнителем воли народной...»<sup>12</sup><br>
        <br>
        8 октября 1928 г. Ершов был осужден по статьям 58-10 и 58-13 УК РСФСР 
        на 10 лет. В 1934 г. он возвратился из заключения, приехал в Москву повидаться 
        с сыном и родственниками. По воспоминаниям родственников, в 1930-е гг. 
        Е. В. Ершов заходил к ним в Сухуми. Они проводили его до пристани, где 
        он сел на судно, которое уходило в неизвестном направлении.<sup>13</sup><br>
        <br>
        Сын Евгения Васильевича воспитывался родственниками жены супругами Гертель. 
        Они был расстреляны как враги народа в 1938 г. После их гибели мальчик 
        воспитывался у своей бабушки А.А. Загурской, урожденной Федотовой. В 1942-1945 
        гг. служил в армии, получил аттестат зрелости, затем специальность переводчика 
        английского языка. Реабилитация его отца Евгения Васильевича Ершова произошла 
        5 октября 1992 г. <br>
