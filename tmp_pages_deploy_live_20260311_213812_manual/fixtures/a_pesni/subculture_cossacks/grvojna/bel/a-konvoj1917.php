  <br>
  Наконец, французы дали визы на въезд во Францию и отправили офицеров на военном 
  транспорте в Марсель. В Париж они прибыли к середине мая. Здесь начались новые 
  затруднения. Англичане, на основании имевшихся бумаг и официального обращения 
  к английскому консулу, разрешили въезд в Англию, откуда можно было отправиться 
  на пароходе на Дальний Восток. Но французские власти не давали визы на выезд 
  из Франции (?!). Возможно, это было проявление тех «дипломатических осложнений» 
  в связи с целью поездки, которые предвидела Императрица Мария Федоровна. <br>
  <br>
