  В 1918 г., состоявшие в Гвардейских Дивизионах Добровольческой армии офицеры 
  Конвоя пытались восстановить прерванную войной с большевиками связь с царской 
  Семьей. Пробраться с Кавказа, через все фронты красных армий в Сибирь, не удалось. 
  Ни Главное командование, ни офицеры, отрезанные сотнями верст от Сибири, не 
  имели никаких точных сведений, кроме неясных и неуверенных слухов о гибели Госудая 
  (о чем свидетельствует нижеприведенный документ, подписанный генералом Лукомским). 
  Офицер Конвоя А.А. Грамотин, совершив почти кругосветное путешествие и добравшийся 
  до места назначения через Европу и Владивосток, писал: «...Вспоминая сейчас 
  о нашем порыве, я сознаю, что это был б[ыть] м[ожет] плод нашего фантастического 
  и мало продуманного плана, который, конечно, не мог бы дать серьезных результатов, 
  если бы не случилось чудо! По нашей тогда молодости мы были столь наивны, что 
  искренне верили в полный успех. План был таков: пробраться в Сибирь, через линии 
  фронтов, «странниками». Перейти большевицкие линии и пытаться достигнуть местопребывания 
  Царской Семьи. Как мы бы ни переодевались, нас сразу же могли разоблачить! Возможно, 
  однако, что в пути мы огрубели бы, и, познакомившись с бытом и жизнью селения, 
  не выделялись бы среди крестьян, «как ворона на снегу». <br>
  <br>
  «...мы горели этим желанием, и стремления наши были Святы! Мы верили, что старообрядцы, 
  как менее поддавшиеся коммунизму, будут нам, «странникам» оказывать полное содействие...» 
  <br>
  <br>
  Сохранилась рекомендация высокого духовного лица своей пастве оказывать содействие 
  Грамотину: «Удостоверение. Предъявитель сего Хорунжий А.А. Грамотин. Просим 
  всех старообрядцев древнеправославной церкви, имеющей Белокриницкую иерархию 
  оказывать должное содействие и посильную помощь, если таковая потребуется ему 
  от Вас. Что удостоверяется подписом с приложением печати. Смиренный Геннадий 
  Епископ Донской». <br>
