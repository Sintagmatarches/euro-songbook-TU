  делал все, чтобы как можно скорее и безболезненее ликвидировать Русскую Армию. 
  Казакам, составлявшим ее большую часть, предлагалось записываться в Иностранный 
  Легион, на работы в Бpaзилию или возвращаться в советскую Россию. В гвардейском 
  дивизионе, на вопросы французов, куда желают ехать, казаки отвечал «В Германию!» 
  С Лемноса полковник Рогожин с Терской Гвардейской сотней вызывается в Конвой 
  Главнокомандующего. Летом 1921 г. началась переброска Сводного Кубанского корпуса 
  (генерал-лейтенант М.А. Фостиков) с Лемноса в Сербию. 1 июня гвардейский дивизион 
  в составе эшелонов корпуса прибыл в город Чевчели. <br>
