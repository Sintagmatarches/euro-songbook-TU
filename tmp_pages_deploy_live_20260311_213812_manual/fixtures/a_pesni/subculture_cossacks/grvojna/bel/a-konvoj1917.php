  в распоряжение Российского Военного Агента в Лондоне, для дальнейшего затем 
  следования в Сибирь в качестве офицера-курьера от Генерала Деникина, к Адмиралу 
  Колчаку». <br>
  <br>
  В Лондоне вновь начались хлопоты о визах на Дальний Восток. Пароходов было мало, 
  и все места расписаны на полгода вперед. Удалось достать два места на японском 
  пароходе при условии, что до Сингапура офицеры должны ехать в третьем классе. 
  <br>
  <br>
  С 12 на 13 июня на переполненном пассажирами пароходе, наконец, отбыли на Дальний 
  Восток. Перед отъездом офицеров приняла Государыня Императрица Мария Федоровна 
  во дворце вдовствующей королевы Англии. <br>
  <br>
  Ее Величество благословила посланцев, пожелав благополучного путешествия. Во 
  дворце Грамотин получил небольшую сумму в английских фунтах, но от кого она 
  исходила — не было известно. <br>
  <br>
  В Гонконге они впервые услышали страшную весть о гибели Государя Императора 
  и о том, что Царская Семья, якобы, «вывезена» большевиками в неизвестном направлении. 
  Не задерживаясь в Японии, офицеры прибыли во Владивосток 8 августа. Во Владивостоке 
  упорно распространялся слух, что вместе с Государем в Екатеринбурге, до взятия 
  его армией Адмирала Колчака 25 июля, в ночь с 16 на 17 (с 3 на 4) июля, погибла 
  вся Царская Семья... <br>
  <br>
  Продолжив путь в Омск, 23 августа офицеры явились в Штаб Верховного Главнокомандующего 
  адмирала Колчака. От Начальника Штаба генерал-лейтенанта Дитерихса Грамотин 
  получил приказание отправиться в распоряжение судебного следователя по особо 
  важным делам Соколова: <br>
  <br>
  «Секретно. Начальник Штаба Верховного Главнокомандующего 26 августа 1919 г. 
  № 14. Кубанского Гвардейского Дивизиона Есаулу Грамотину. <br>
  <br>
  Приказываю Вам с получением сего отправиться в распоряжение Судебного Следователя 
  по особо важным делам Соколова и состоять при нем по выполнению возложенных 
  на него волей Верховного Правителя обязанностей по производству предварительного 
  следствия особой важности. <br>
  <br>
  Начальник Штаба Верховного Главнокомандующего Генерального Штаба Генерал-Лейтенант 
  Дитерихс». <br>
  <br>
  Гибель Царской Семьи и лиц, при них находившихся, следственными властями точно 
  установлена: 25 февраля 1919 г. Это подчеркнуто для того, чтобы разъяснить то 
  естественное недоумение, которое возникает при сравнении других дат: <br>
  <br>
  1. Екатеринбургское злодеяние было совершено в ночь с 16 на 17 июля 1918 г. 
  <br>
  <br>
  2.Документ о командировке в Сибирь офицера Конвоя, по женланию Императрицы Марии 
  Федоровны, подписан помощником Главнокомандующего Добровольческой Армии Юга 
  России 29 января 1919 г.<br>
  <br>
  Объяснением тому, что эта командировка произошла через пять с лишним месяцев 
  после совершенного злодеяния, служит то, что до 25 февраля 1919 г., кроме вести 
  о мученической кончине Государя Императора, не было еще точных сведений о судьбе 
  всей Царской Семьи не только на Юге России, но и в самой Сибири. <br>
  <br>
  «Перед нашим прибытием в Омск, — вспоминал А.А. Грамотин, - большевики летом 
  1919 г. перешли в большое наступление и угрожали Екатеринбургу. Предстояла эвакуация 
  и Омска. В последних числах августа (т.е. вскоре после нашего поступления в 
  его распоряжение), следователь Соколов из Омска переехал в Читу. Мы с Булыгиным 
  его сопровождали и охраняли весь его следственный материал. Соколов и все, кто 
  с ним работал, трепетно переживали кошмар Екатеринбургского злодеяния, подробности 
  которого точно были установлкны следственными властями». <br>
  <br>
  Есаул Грамотин 3 февраля получил предписание от Походного Атамана Дальневосточных 
  Казачьих Войск «отправиться в Лондон и Париж курьером с депешами и возвратиться 
  обратно в Россию». В марте 1920 г. следователь Соколов, в связи с общей обстановкой, 
  сложившейся в Сибири, принужден был оставить Читу и выехать в Харбин. Капитан 
  Булыгин и есаул Грамотин его сопровождали. Из Харбина, Соколов и Булыгин в 20-x 
  числах марта с документами и найденными во время производства слаедствия Реликвиями 
  Царской Семьи отбыли в Европу, а Грамотин вынужден был остаться в Харбине.<br>
  <br>
  Так печально закончилась командировка есаула Грамотина. <br>
