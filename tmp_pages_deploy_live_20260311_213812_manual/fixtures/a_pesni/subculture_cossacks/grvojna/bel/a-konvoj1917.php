  Выполняя приказание командира Дивизиона, хорунжий Грамотин отправился в Крым, 
  поступил в формируемый отряд охраны лиц Императорской Фамилии и познакомился 
  с офицером Л[ейб]-гв[ардии] Петроградского полка П.П. Булыгиным, инициатором 
  создания отряда. Главная часть охраны находилась в имении «Харакс», где пребывала 
  Императрица Мария Федоровна, вместе с семьей Великой Княгини Ольги Александровны. 
  Капитан Булыгин был начальником Харакского отряда. С этим молодым, но очень 
  серьезным, религиозным и всей душой преданным Царской Семье офицером, Грамотин 
  подружился. Они решили, во что бы то ни стало пробраться в Сибирь и, если нельзя 
  будет установить непосредственную связь с Царской Семьей, то, во всяком случае, 
  получить самые точные сведения. <br>
  <br>
  Решение стало известно Императрице и одобрено Ею. В конце декабря 1918 г., капитан 
  Булыгин и хорунжий Грамотин были приняты Ее Величеством, после чего Государыня 
  Императрица пригласила офицеров к обеду. Беседуя с ними о предстоящем путешествии, 
  Государыня предвидела неминуемые осложнения из-за получения виз и прочие затруднения 
  «дипломатического характера», в связи с целью поездки. Выезд совпал с отбытием 
  Великой Княгини Ольги Александровны с семьей из Крыма на Кубань. <br>
  <br>
  По желанию Императрицы, Грамотин сопровождал Великую Княгиню до Новороссийска 
