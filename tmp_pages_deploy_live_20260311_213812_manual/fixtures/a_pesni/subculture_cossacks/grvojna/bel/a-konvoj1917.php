	<span class="text"><strong>П.Н. Стрелянов (Калабухов)</strong><br>
  <br>
        <span class="header"><strong>СОБСТВЕННЫЙ ЕГО ИМПЕРАТОРСКОГО ВЕЛИЧЕСТВА 
        КОНВОЙ ПОСЛЕ ФЕВРАЛЯ 1917 г.</strong><br></span>
  <br>
        <em>Альманах «Белая гвардия», №8. Казачество России в Белом движении. 
        М., «Посев», 2005, стр. 92-96</em>.<br>
  <br>
  <br>
  История Собственного Его Императорского Величества (Е.И.В.) Конвоя в военно-исторической 
  литературе прерывается мартом 1917 г. Вместе с тем, переформированный а Гвардейские 
  Дивизионы — Кубанский и Терский, Конвой принял участие в Гражданской войне и 
  оставался затем единственной строевой воинской частью Русской армии, сохранившейся 
  в эмиграции. О борьбе в годы русского лихолетья с предателями России офицеры 
  Конвоя вели личные записи. Почти все погибло в боевой обстановке Второй Мировой 
  войны. В предлагаемом кратком очерке продолжена история части (до ее переезда 
  с о. Лемнос в Сербию), подготовленная на основе публикаций русского зарубежья, 
  воспоминаний конвойцев и архивных данных. <br>
  <br>
  К концу февраля - началу марта 1917 г. Собственный Е.И.В. Конвой, после командировок 
  (сотнями посменно) на Западный фронт и участия в боевых действиях, нес повседневную 
  службу по охране императора и его семьи. <br>
  <br>
  Сотни Конвоя были дислоцированы следующим образом: <br>
  <br>
  - Лейб-гвардии 1-я Кубанская (есаул Г. Рашпиль) и Лейб-гвардии 4-я Терская (есаул 
  Г. Татонов) сотни — в Ставке Верховного Главнокомандующего в Могилеве; <br>
  <br>
  - Лейб-гвардии 2-я Кубанская (есаул М. Свидин) и Лейб-гвардии 3-я Терская (есаул 
  К. Панкратов) сотни — в Царском Селе, где проживала императрица Александра Федоровна 
  с детьми; <br>
  <br>
  - пешая полусотня Лейб-гвардии 5-й Сводной сотни (хорунжий А. Рогожин) — в Киеве 
  при вдовствующей императрице Марии Федоровне; <br>
  <br>
  - команда продолжавшей свое формирование Лейб-гвардии 5-й Сводной сотни (есаул 
  В. Савицкий) и нестроевая команда — в Петрограде, по охране денежного ящика, 
  казенного имущества Конвоя и обслуживания лошадей сотни. <br>
  <br>
  С отречением Николая II от престола (2 марта), положение Конвоя стало неопределенным. 
  Временное правительство решило расформировать Конвой, а офицеров и казаков отправить 
  в полки кавказских казачьих войск. Генерал Алексеев, желая иметь при себе дисциплинированную 
  и стойкую воинскую часть, принимал меры к оставлению дивизиона Конвоя в Ставке. 
  <br>
  <br>
  Офицеры не воспользовались правом подать в отставку и, исполняя волю императора 
  — служить так же верно Родине, как служили ему, — считали своим долгом сохранить 
  часть, и с ней выступить на фронт. <br>
  <br>
  Старые соратники лейб-казаки писали с фронта, что Конвой ждут, что с прибытием 
  его предполагалось формирование своей дивизии из всех гвардейских казачьих частей. 
  13 марта 1917 г. был издан приказ Главнокомандующего войсками Петроградского 
  военного округа на театре военных действий №12835 генерал-лейтенанта Л.Г. Корнилова: 
  «переименовать бывший Собственный Е.В. Конвой в Л[ейб]-Гв[ардии] Кавказский 
  казачий полк и отправить его в Действующую армию с включением в 3-ю Гвардейскую 
  кавалерийскую дивизию», неисполненный в силу сложившейся ситуации. <br>
  <br>
  Начальники Войсковых штабов Кубанского и Терского казачьих войск (ККВ и ТКВ) 
  просили Военный Совет о скорейшем прибытии сотен Конвоя в расположение войск. 
  30 марта 1917 г. Лейб-гвардии 2-я Кубанская и 3-я Терская сотни, несшие службу 
  в Царском Селе, а также команда казаков Лейб-гвардии 5-й Сводной сотни из Петрограда 
  отбыли в распоряжение Наказных атаманов в свои войска. <br>
  <br>
  Другой дивизион Конвоя Лейб-гвардии 1-я Кубанская и 4-я Терская сотни, находившиеся 
  в Ставке Верховного Главнокомандующего, вышли непосредственно из Могилева в 
  первых числах апреля и, как и полусотня Лейб-гвардии 5-й Сводной сотни из Киева, 
  прибыли на Кавказ позже царскосельского дивизиона.<br>
  <br>
  Конвой был переформирован два отдельных — Кубанский и Терский гвардейские дивизионы. 
  В апреле месяце, в Екатеринодаре, Кубанский гвардейский дивизион направлялся 
  в Войсковой штаб для сдачи на хранение Георгиевских штандартов. Очевидец (тогда 
  подъесаул 1-го Кавказского полка ККВ Ф.И. Елисеев, находившийся в командировке 
  с фронта в городе) свидетельствует: «Мы с братьями идем по Красной улице. Издали 
  видна длинная колонна конных казаков. Гуляющая публика остановилась и ждет ее. 
  Ждем и мы. Узнаем, что идет Конвой Государя... Их две сотни казаков. Все одеты 
  однообразно в черкески защитного цвета с кручеными желтыми гвардейскими жгутами 
  на них, вместо погон. Красные бешметы, обшитые желтой тесьмой и в крупных черных 
  папахах с красными верхами. Под всеми, словно на подбор, темно-гнедые кабардинские 
  кони с черными гривами и хвостами. У всех холодное оружие в серебре. Офицеры 
  одеты особенно щегольски. Под ними нарядные лошади. Седельная сбруя в кавказском 
  серебре. Два императорских штандарта с двуглавыми орлами на древках поверх черных 
  чехлов парили над строем конвойцев и говорили всем, что это идет «особенная 
  строевая часть». <br>
  <br>
  Сотни шли спокойным шагом в колонне «по-шести», абсолютно молчаливо и своим 
  нарядным видом так загипнотизировали публику, что она молча, сосредоточенно 
  и торжественно смотрела на казачий конный строй, как на сказочное видение, которое 
  она встречает впервые в своей жизни и которое уже не повторится н и к о г д 
  а...». <br>
  <br>
  14 июня в Екатеринодаре отдается приказ по Конвою №130: «На основании положения 
  Военного Совета от 27 апреля сего года Конвой Верховного Главнокомандующего 
  (такое наименование, после отречения Николая II, Собственный Е.И.В. Конвой получил 
  лишь номинально, не состоя в этом качестве на службе ни одного дня - П.C.) переформирован 
  в Кубанский и Терский гвардейские казачьи дивизионы, посему гг. офицеры Кубанского 
  войска: полковник Рашпиль, есаулы Свидин, Савицкий, Ветер, подъесаулы Скворцов, 
  Белый, Шведов, Шкуропатский, соатник Зборовский, хорунжие Нагаец, Галушкин и 
  Грамотин перечисляются в Кубанский гвардейский казачий дивизион, а Терского 
  войска: есаулы Татонов, Панкратов, Макухо, подъесаул Федюшкин, сотники Зерщиков, 
  Скляров, Рогожин, хорунжие Колесников, Федюшкин, Лавров, Ергушов, прикомандированный 
  сотник Вертепов и ветеринарный врач н[ижний] ч[ин] Борисов — в Терский гвардейский 
  казачий дивизион... Вр. командир Конвоя полковник Киреев».<br>
  <br>
  На своих войсковых территориях дивизионы в составе Добровольческой армии вступили 
  в вооруженную борьбу с большевиками. Начался Первый Кубанский («Ледяной») поход. 
  22 февраля 1918 г. в Екатеринодаре командир Кубанского гвардейского дивизиона 
  полковник Рашпиль принял участие в совещании у Войскового атамана, на котором 
  было решено: ввиду наступления превосходящих сил красных и возможности окружения, 
  всем верным правительству частям оставить город. В Первый Кубанский поход выступили 
  офицеры: барон М.Л. Унгерн фон Штернберг, Г.А. Рашпиль, М.И. Свидин, В.Д. Савицкий, 
  И.А. Ветер, М.А. Скворцов, В.Э. Зборовский, Е.Д. Шкуропатский, Н.В. Галушкин 
  (все — по состоянию на 1917 г.) и причисленные во время похода к гвардейскому 
  дивизиону П.С. Есаулов и А.М. Лекторский. <br>
  <br>
  Часть гвардейцев вошла в конный отряд (командир — Генерального штаба полковник 
  Кузнецов), 6 марта оторвавшийся от главных сил и, начиная с 9 марта, отступавший 
  из аула Шенджий с боями в горы на Тубинский перевал. В двадцатых числах марта 
  отряд был окружен большевиками, и лишь нескольким офицерам гвардейского дивизиона 
  и 1-го Екатеринодарского полка удалось избежать плена и провести остаток зимы 
  в шалашах в горном лесу. После освобождения летом 1918 г. от красных Екатеринодара 
  и северной части Кубани, они влились в свои части. <br>
  <br>
  19 марта в станице Ново-Дмитриевской, после соединения Добровольческой армии 
  с Кубанским правительственным отрядом, произошла встреча группы лейб-казаков 
  и кадра Конвоя. В составе конвойцев находились: полковник Рашпиль, есаулы Ветер 
  и Галушкин. По ходатайству полковника Рашпиля и с согласия генерала Корнилова, 
  в конном отряде генерала И.Г. Эрдели была сформирована отдельная гвардейская 
  казачья сотня. В сотне, под командованием полковника Рашпиля, состояли: 1-й 
  Лейб-Казачий взвод есаула Рыковского, 2-й взвод — офицеры и казаки Конвоя, 3-й 
  и 4-й взводы — казаки Кубанского Варшавского дивизиона; всего около 100 шашек. 
  <br>
  <br>
  31 марта под Екатеринодаром, в атаке гвардейской сотни на многократно превосходящих 
  ее красных, убит полковник Г.А. Рашпиль. В этом бою гвардейцы потеряли четверть 
  своих людей и три четверти лошадей. <br>
  <br>
  В августе 1918 г., в освобожденном Екатеринодаре, хоронили полковника Рашпиля. 
  В траурной процессии с хором трубачей и Войсковым хором песельников, за катафалком 
  с гробом погибшего, старый урядник-конвоец с седой бородой вел высокого гнедого 
  кабардинца под седлом с черным траурным чепраком... <br>
  <br>
  Кубанский гвардейский дивизион пополнился в дальнейшем из рядов конного дивизиона 
  под командой полковника Белого, прибывшего с Тамани, и к 10 декабря 1918 г. 
  входил в 1-ю Кубанскую казачью дивизию, имея в своем составе 150 шашек при 5 
  пулеметах. <br>
  <br>
  Весной 1919 г. командиром дивизиона, с производством в полковники, становится 
  В.Э. Зборовский. Гвардейцы входили в состав 1-й Кубанской казачьей дивизии (генерал-майор 
  В.В. Крыжановский) и участвовали в боях под Царицыном. В июле полковник Зборовский 
  назначается командиром бригады этой дивизии, в декабре — начальником Кавказской 
  Горской дивизией. Кубанский гвардейский дивизион принимает полковник Свидин, 
  до вступления полковника Зборовского вновь в командование частью в марте 1920 
  г. Старшие офицеры Конвоя возглавляли в Гражданской войне соединения Добровольческой 
  армии и ВСЮР. Полковник Шапринский в 1918 г. состоял командиром 1-го Лабинского 
  полка ККВ, в 1919 г. в чине генерал-майора — командиром бригады 2-й Кубанской 
  казачьей дивизии. <br>
  <br>
  Полковник Скворцов последовательно командовал 2-м Запорожским полком ККВ, бригадой 
  4-й Кубанской казачьей дивизии, с июня 1919 г. — начальник дивизии, в сентябре 
  вр.и.д. командующего 2-м Кубанским казачьим корпусом, произведен в генерал-майоры. 
  <br>
  <br>
  В январе 1920 г. Кубанский гвардейский дивизион был переименован в Атаманский 
  конный полк. Перед эвакуацией Екатеринодара два взвода казаков со штандартами 
  были направлены в Новороссийск и отплыли в Крым; почти все офицеры, ввиду капитуляции 
  Кубанской армии (под Адлером — Сочи), также ушли морем в Севастополь. <br>
  <br>
  Полк отходил с правительственным отрядом на Туапсе. Казаки (до 400 шашек) во 
  главе с временно командующим полком есаулом Б. Ногайцем сначала влились во 2-ю 
  Кубанскую казачью дивизию, а затем, видимо, распылились. Часть их поступила 
  в Русскую армию, есаул Б. Ногаец был расстрелян красными в Екатеринодаре в 1920 
  г. <br>
  <br>
  Терский гвардейский дивизион по прибытии в 1917 г. на войсковую территорию, 
  11 июня стал в пригороде Владикавказа колонии Михайловская. Временным командиром 
  части назначается есаул Татонов, временным его помощником есаул Панкратов. С 
  начала Белой борьбы большинство офицеров и казаков дивизиона участвовало в Терском 
  восстании против красных в 1918 году или, как хорунжий Лавров, воевали в Киеве 
  в отряде генерала от кавалерии графа Ф.А. Келлера, а затем в Одессе в отряде 
  генерал-майора А.Н. Гришина-Алмазова. <br>
  <br>
  1 августа 1919 г. в Пятигорске формируется Терский гвардейский атаманский дивизион, 
  состоящий из двух сотен, под командованием полковника Макухо. Того же числа, 
  приказом по Дивизиону №1 в него зачисляются: подъесаул А. Федюшкин — помощник 
  командира, сотник Рогожин, хорунжий Колесников — адъютант, хорунжий Н. Федюшкин 
  — вр.и.д. казначея, хорунжий Лавров — командир 1-й сотни, хорунжий Ергушев и 
  прикомандированный хорунжий Ромащенко. <br>
  <br>
  В части состояло 7 подхорунжих: П. Солнышкин, П. Фомин, Я. Солнышкин, С. Гавриш, 
  С. Асташев, Н. Сиволов, А. Лещенко; 2 вахмистра, 8 старших урядников, 8 младших 
  урядников, 16 приказных, 256 казаков и 7 трубачей. <br>
  <br>
  Терцы-гвардейцы участвовали в боях с красными на Свято-Крестовском фронте. 17 
  сентября 1919 г. приказом по Дивизиону №15 в его состав вошла пулеметная команда 
  (3 пулемета) под командованием прапорщика Доценко. 6 октября приказом №34 зачислены 
  прикомандированными к 1-й сотне: 1-го Кизляро-Гребенского полка сотник Донсков 
  и штаба 2-й Терской казачьей дивизии хорунжий Вертепов. <br>
  <br>
  Весной 1920 г. офицеры и казаки Терского дивизиона с боями отошли по территории 
  войска и, через Грузию, прибыли 27 июня без лошадей в Крым. Распоряжением Войскового 
  атамана генерал-лейтенанта Г.А. Вдовенко дивизион был переформирован в Терскую 
  гвардейскую сотню. До получения лошадей, сотня временно прикомандировывалась 
  к Терско-Астраханскому запасному дивизиону и располагалась в Северной Таврии, 
  обучаясь пешему бою. <br>
  <br>
  22 июля Терскую гвардейскую сотню в ожидании десанта на Кубань перебросили в 
  Керчь, но на первые транспорты она не попала. Здесь сотня вошла в только что 
  сформированный Сводно-казачий дивизион полковника Усачева. Помощником командира 
  дивизиона был назначен командир Терской сотни полковник Рогожин, а сотню принял 
  есаул Щербаков. В городе остался кадровый взвод гвардейской сотни — 4 офицера 
  и 25 казаков, а транспорты с дивизионом 8 августа отошли от Керчи. <br>
  <br>
  В ночь с 9 на 10 августа отряд десанта высадился у станицы Таманской. 16 августа 
  сотня выдвинулась к станице Вышестеблиевской, и в ночной атаке 17-го, в авангарде 
  отряда генерала А.Н. Черепова, с налета взяла гору Нефтянка. <br>
  <br>
  Сбив красных, гвардейцы окопались, удерживая высоту. В течение всего дня красная 
  артиллерия, в том числе крупных калибров, обстреливала позицию. Фляги у казаков 
  были пусты, подвоз пищи из станицы на саму гору был невозможен. Прорвалась под 
  обстрелом лишь повозка с едой казначея дивизиона хорунжего Потапова. Две колонны 
  подкрепления кубанцев-бредовцев были рассеяны шрапнелью большевиков. В три часа 
  ночи гвардейская сотня спустилась с высоты в сторону противника. <br>
  <br>
  Весь световой день до вечера 18 августа, в непрерывных атаках на нее красной 
  конницы с тачанками и пехоты, под артиллерийским обстрелом, сотня безлошадных 
  казаков прикрывала отход своих частей. На крики красноармейцев: «Сдавайтесь!», 
  из казачьих рядов слышался гордый ответ старых наполеоновских гвардейцев: «Гвардия 
  не сдается, а умирает!» <br>
  <br>
  19 августа пароходы и баржи с войсками десанта вышли в море. За два дня боев 
  в красном кольце Гвардейская сотня потеряла убитыми двух казаков, ранеными и 
  контуженными двух офицеров и 14 казаков. <br>
  <br>
  За отличия в бою 18 августа 1920 г., приказами по Терскому казачьему войску 
  были произведены в следующие чины: Терской Гвардейской сотни есаул Щербаков, 
  хорунжие Поморцев и Барагунов, прикомандированный к сотне хорунжий Асташов. 
  За мужество и храбрость в том же бою, командиром Сводно-казачьего дивизиона 
  были переименованы в следующие звания 17 урядников и казаков. <br>
  <br>
  Во время десанта на Кубань были тяжело ранены Кубанского Дивизиона полковник 
  Зборовский и есаул Е. Ногаец. <br>
  <br>
  В 1920 г., в последних боях Русской Армии в Крыму, при эвакуации на о. Лемнос 
  Кубанский гвардейский дивизион под командованием полковника Зборовского состоял 
  во 2-й Кубанской казачьей дивизии (генерал-майор С.Ф. Цыганков). <br>
  <br>
