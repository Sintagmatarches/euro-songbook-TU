  Кубанское Софиевское военное училище <br>
  <br>
  7. Для охраны побережья имелся небольшой «Кубанский флот» и береговая охрана 
  <br>
  <br>
  В течение Гражданской войны организация высших соединений несколько раз менялась. 
  Конные части со своей артиллерией обыкновенно составляли 3 Кубанских конных 
  корпуса. Пластуны работали почти все время бригадами, будучи придаваемы конным 
  корпусам. <br>
  <br>
  В моменты наивысшего напряжения (конец 1918 г.) кубанские части обыкновенно 
  пополнялись сверх нормы и давали сверхкомплектные полки, так, например: Хоперский 
  полковой округ, кроме постоянных 1-го и 2-го Хоперских и 2-го Кубанского партизанских 
  полков давал добрую половину в 1-й Кубанский полк и с начала восстания до февраля 
  1919 г. имел 3-й и 4-й Хоперские полки; Лабинский полковой округ, кроме 2-го 
  Лабинского полка в районе г. Киева, ушедшего с отрядом генерала Бредова в Польшу, 
  с началом 1920 г. выставил 4 конных полка: 1-й, 2-й, 3-й и 4-й Лабинские. <br>
  <br>
  В общем же Кубанцы в Гражданскую войну для борьбы с большевиками в 1918-1920 
  гг. выставляли: <br>
  А) Конницы — 37 полков (31 Кубанский казачий, 4 Черкесских и 2 Карачаевских); 
  <br>
  Б) Пехоты — 12 пластунских батальонов; <br>
  В) Артиллерии — 24 батареи. <br>
  <br>
        Кроме того, некоторые части Добровольческой Армии состояли иногда наполовину 
        из Кубанских казаков и, таким образом, как выяснилось, по подсчету в 1919 
        г., Добровольческая армия на 85% состояла из Кубанцев, численность которых 
        временами доходила до 110 тысяч бойцов. <br>
