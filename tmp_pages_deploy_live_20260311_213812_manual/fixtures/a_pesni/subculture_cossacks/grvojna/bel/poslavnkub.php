  О СЛАВНОМ КУБАНСКОМ ПАРТИЗАНЕ</strong> <br>
	</span>
  <br>

	По Кавказу, по Кубани<br>
	  Слышен грозный крик «Ура!»<br>
	  Разразился над врагами<br>
	  Генерал-герой Шкура.<br>
	<br>
	<em>Припев:</em><br>
	<br>
	  Грянем славу трубой,<br>
	  Славный наш Шкура-герой,<br>
	  Мы с твоим лихим отрядом<br>
	  Скоро будем под Москвой.<br>
	  Раньше с маленьким отрядом,<br>
	  Чуть не в десять человек,<br>
	  Смерть он нес, как тучи с градом —<br>
	  Не забыть того вовек...<br>
	<br>Ночью темною, бывало,<br>
	  На станицу налетит<br>
	  И атакою удалой<br>
	  Бунт нелепый укротит.<br>
	<br>
	И не раз с отрядом малым<br>
	  Города он в руки брал,<br>
	  Этот грозный и удалый<br>
	  Славный Шкуро-генерал.<br>
	<br>Тихо стало на Кубани,<br>
	  Покорен уже Кавказ,<br>
	  Но мы знали и заране,<br>
	  Что не скоро мира час.<br>
	<br>Златоглавую столицу<br>
	  Раньше нужно отстоять,<br>
	  А тогда уж и в станице<br>
	  Можно мирно поживать.<br>
	<br>Шкуро на Дон появился<br>
	  В помощь брату своему,<br>
	  Вновь на бой он снарядился,<br>
	  Прокричим «Ура!» ему...<br>
	<br>Только вечер — партизаны<br>
	  Волчьей сотни — «Марш вперед!».<br>
	  Тихо крадутся кустами,<br>
	  А впереди Шкура идет.<br>
	<br>Кто попался — не сорвется,<br>
	  Головы уж не снесет:<br>
	  Под ударом вмиг свернется,<br>
	  Душу сразу отдает.<br>
	<br>И над Доном слышна слава:<br>
	  Генерал пришел Шкура,<br>
	  Рубит влево, рубит вправо,<br>
	  И кричат ему: «Ура!»<br>
