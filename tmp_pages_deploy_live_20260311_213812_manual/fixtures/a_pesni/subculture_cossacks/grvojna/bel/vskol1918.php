  В память дедов и отцов -<br>
  Вновь свободно стало племя<br>
  Возродившихся Донцов.<br>
  <br>
  Славься, Дон, и в наши годы,<br>
  В память вольной старины,<br>
  В час невзгоды - честь свободы.<br>
  Отстоят твои сыны.<br>
