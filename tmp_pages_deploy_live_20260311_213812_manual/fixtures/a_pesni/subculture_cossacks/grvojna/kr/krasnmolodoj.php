        1. На разведке боевой</strong><br>
        <br>
        Красноармеец молодой <br>
        На разведке боевой. <br>
        Э-э-э-э-э, герой! <br>
        На разведке боевой. <br>
        На разведку он ходил, <br>
        Все начальству доложил. <br>
        Э-э-э-э-э, герой! <br>
        Все начальству доложил. <br>
        Ночка тёмна, нету звезд, <br>
        Вышел вражеский разъезд. <br>
        Э-э-э-э-э, герой! <br>
        Вышел вражеский разъезд. <br>
        Только спрятаться хотел — <br>
        В чистом поле не успел. <br>
        Э-э-э-э-э, герой! <br>
        В чистом поле не успел. <br>
        Тут разъезд его схватил <br>
        И к допросу приступил. <br>
        Э-э-э-э-э, герой! <br>
        И к допросу приступил: <br>
        «Ты скажи, скажи нам, брат, <br>
        Сколько есть у вас солдат? <br>
        Э-э-э-э-э, герой! <br>
        Сколько есть у вас солдат? <br>
        Сколько конных впереди, <br>
        Сколько пеших позади? <br>
        Э-э-э-э-э, герой! <br>
        Сколько пеших позади? <br>
        Если скажешь, наградим – <br>
        Чин полковника дадим. <br>
        Э-э-э-э-э, герой! <br>
        Чин полковника дадим. <br>
        А не скажешь, как шпион <br>
        Будешь нами расстрелён. <br>
        Э-э-э-э-э, герой! <br>
        Будешь нами расстрелён». <br>
        «Ничего вам не скажу, <br>
        За Советску власть служу! <br>
        Э-э-э-э-э, герой! <br>
        За Советску власть служу!» <br>
