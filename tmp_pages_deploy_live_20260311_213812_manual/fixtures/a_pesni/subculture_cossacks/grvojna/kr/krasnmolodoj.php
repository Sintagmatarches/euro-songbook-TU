        Переработанный, сокращенный вариант старой солдатской песни периода русско-японской 
        войны о солдате 84-го пехотного Чембарского полка Василии Рябове, сюжет сохранен. Василий Рябов был призван на войну из запаса. После поражения у Ляояна в августе 1904 г. русская армия Куропаткина отошла к столице Маньчжурии Мукдену. По слухам, Куропаткин готовил наступление. 33-летний Василий Рябов вызвался в числе охотников разведать позиции японцев и в одежде китайского крестьянина 27 (14 ст. ст.) сентября пробрался на японскую передовую, где был пойман. Его приговорили к смертной казни и 30 сентября расстреляли. Японские штабисты составили письмо, в котором высказали &quot;полное уважение&quot; и &quot;высший предел сочувствия&quot; поведению Рябова. Письмо оставлено на нейтральной территории и найдено казачьим разъездом. См.: А.А. Игнатьев. Пятьдесят лет в строю. М..: Госиздат, 1950. С. 288. <br>
        <br>
Штык в груди, упоминаемый в песне - домысел. По народной версии истории, Рябова жестоко пытали японцы.<br>
