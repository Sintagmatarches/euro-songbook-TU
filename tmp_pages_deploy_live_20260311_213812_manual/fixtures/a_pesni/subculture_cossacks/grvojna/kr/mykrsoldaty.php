        <br>
        Песня была очень распространенной, известна под разными названиями: &quot;Мы 
        - красные солдаты&quot;, &quot;Песня красных стрелков&quot; и т. д. Бытовало 
        множество вариантов. В данном варианте есть характерная черта: <em>&quot;Бандиты 
        отступали...&quot;</em> - это могли быть и белые, но вполне могли быть 
        и повстанцы. Основой послужила популярная народная песня <a href="../../popular20/sredilesov.php">«Про 
        Чуркина-атамана»</a>, которую пели и во время Первой мировой, и во время 
        Гражданской: <br>
        <br>
        <em>Среди лесов дремучих<br>
        Разбойнички идут<br>
        И на плечах могучих<br>
        Товарища несут…</em> <br>
        <br>
        Во время Первой мировой припев исполнялся так: <br>
        <br>
        <em>Все пушки, пушки грохотали,<br>
        Трещал наш пулемет,<br>
        А немцы отступали,<br>
        Мы двигались вперед! </em><br>
        <br>
        Песня была особенно популярна в Прибайкалье и вообще в Сибири (под заглавием 
        &quot;Песня красных стрелков&quot;). Газета Пятой армии и Восточно-Сибирского 
        военного округа (Иркутск) называлась аналогично - &quot;Красный стрелок&quot;. 
        По воспоминаниям писателя Ю. Либединского, эту песню пела его рота летом 
        1920 года в Барнауле - то есть, какой-то из вариантов этой песни пели 
        красноармейцы при подавлении народных восстаний 1920 года на Алтае - анархического 
        мятежа Григория Рогова и выступления Народной повстанческой армии. <br>
        <br>
