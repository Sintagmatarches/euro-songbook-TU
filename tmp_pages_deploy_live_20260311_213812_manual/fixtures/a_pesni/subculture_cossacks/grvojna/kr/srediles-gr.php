        <a name="1"></a>1. Среди лесов дремучих</strong><br>
        <br>
        Среди лесов дремучих <br>
        Красные идут,<br>
        В своих руках могучих<br>
        Оружие несут.<br>
        Все пушки, пушки грохотали,<br>
        Стрелял наш целый взвод,<br>
        А банды белых отступали,<br>
        А взвод наш шел вперед.<br>
        Бандиты все поразбежались<br>
        И скрылись за туман,<br>
        Один из них попался,<br>
        Сам грозный атаман.<br>
        «Скажи, беляк проклятый,<br>
        Сколько душ ты загубил?<br>
        Скажи, про то все знают,<br>
        Как мужиков ты бил».<br>
        Оружье наше не простое<br>
        Кровью скреплено,<br>
        А сердце наше молодое<br>
        Решимости полно.<br>
        Прогоним всех мы атаманов,<br>
        Буржуев разорим<br>
        И знамя красной свободы<br>
        Над миром водрузим.<br>
        А пушки, пушки все грохочут,<br>
        Под грохот все вперед,<br>
        Над нами светит ясно солнце,<br>
        За нами весь народ.<br>
        <em><br>
        Элиасов Л. Е. Народная революционная поэзия Восточной Сибири эпохи гражданской 
        войны. Улан-Удэ, 1957, стр. 105-106. Записана от бывшего партизана В. 
        Р. Гурьянова.</em><br>
        <br>
        <br>
        Боевая походная песня партизан Прибайкалья. От старой песни сохранилась 
        лишь переделанная строфа припева. Дореволюционный сибирский вариант песни 
        см., например: Старый фольклор Прибайкалья, стр. 4, текст № 6.<br>
        <br>
