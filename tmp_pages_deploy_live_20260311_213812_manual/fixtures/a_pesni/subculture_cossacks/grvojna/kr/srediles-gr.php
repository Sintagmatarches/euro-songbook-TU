        <br>
        Припев в такой же редакции исполнялся в годы Первой мировой войны (только 
        вместо &quot;кадетов&quot; фигурировали &quot;германцы&quot; или &quot;немцы&quot;). 
        В тексте могли упоминаться конкретные исторические лица (см. <a href="sredipesk.php">&quot;Среди 
        песков сыпучих&quot;</a> о Чапаеве). На основе песни про Чуркина-атамана 
        в годы гражданской была также сложена популярная песня <a href="mykrsoldaty.php">&quot;Мы 
        - красные солдаты&quot;</a>.<br>
