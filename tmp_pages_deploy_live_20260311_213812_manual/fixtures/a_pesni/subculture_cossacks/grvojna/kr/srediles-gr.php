        Одна из многочисленных переделок народной песни <a href="../../popular20/sredilesov.php">про 
        Чуркина-атамана</a>, восходящей к стихотворению Федора Миллера «Погребение 
        разбойника» (1846, перевод из нем. поэта Фердинанда Фрейлиграта). Этот 
        вариант бытовал в годы Гражданской войны у красных кубанских казаков. 
        Известны и более ранние казачьи переделки этой песни (см., например, <a href="../../kazaki/semirek/neslesov.htm">&quot;Не 
