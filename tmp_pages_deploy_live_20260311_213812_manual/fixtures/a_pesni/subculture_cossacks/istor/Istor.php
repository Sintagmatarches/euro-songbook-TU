 <span class='top'>ТРАДИЦИОННЫЕ <br>
        ИСТОРИЧЕСКИЕ ПЕСНИ И БАЛЛАДЫ<br>
         XIII - XIX вв.</span><br>
          <br>
        &quot;Традиционные&quot; русские исторические песни - сложенные преимущественно тоническим стихом (а не стихом силлабо-тоническим, то есть основанном на упорядоченном чередовании ударных и безударных слогов), обычно без рифмы, и исполняемые в модальных (а не в тональных - мажоре и миноре) ладах. До XVII в. для русской культуры &quot;поэтическое&quot; и &quot;песенное&quot; были синонимами. Тексты либо пелись и были одновременно поэзией (былины, песни, молитвы), либо говорились (законодательные акты, например). Позднее противопоставление пения и не-пения сменилось противопоставлением стиха (петь который уже не обязательно, а иногда и невозможно) и прозы. Тонические песни никогда не исчезали полностью, вошли и в литературную традицию, а в 2010-е гг. на эстраде даже оттеснили силлабо-тонику на второй план (русский рэп, как правило, тонический).<br>
        <br>
