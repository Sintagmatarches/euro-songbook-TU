<h1>АНИКА-ВОИН</h1>

	<br>
	Жил да был на земле <br>
  Храбрые человек Аника. <br>
  Много он полонил, <br>
  Много он покорил <br>
  Царей и царевичей, <br>
  Королей, королевичей <br>
  И сильных-могучих богатырей. <br>
  Собирается Аника <br>
  В Ерусалим-град: <br>
  Хочет Аника <br>
  Святую святыню погубить, <br>
  Господень гроб разорить. <br>
  Едет Аника через поле, <br>
  Навстречу Анике едет Чудо: <br>
  Голова у Чуда человеческа, <br>
  Власы у Чуда до пояса, <br>
  Тулово у Чуда звериное, <br>
  А ноги у Чуда лошадиные. <br>
  Аника на коне становился <br>
  И этому Чуду дивился: <br>
  «Скажи ты мне, Чудо, проведай, <br>
  Царь ли ты, ли царевич, <br>
  Король ли ты, королевич, <br>
  Или ты сильные-могучий бог<strong>а</strong>тырь?» <br>
  Чудо ему отвечает: <br>
  «Я не царь, не царевич, <br>
  Не король-королевич, <br>
  И не сильный-могучий богатырь, <br>
  А я смерть, страшна и грозна, <br>
  Вельми непомерна, <br>
  Сердцем своим непосульна. <br>
  Кто сотворил небо и землю, <br>
  Кто сотворил облаки, звезды, <br>
  Тот меня сотворил, <br>
  По всей земле попустил. <br>
  Где кого застану, искош<strong>а</strong>ю: <br>
  В пути, в дороге застану - искошаю,<br>
  В избе, на подворье застану - искошаю.<br>
  Хочу и тебя, Аника, искос<strong>и</strong>ти». <br>
  Возговорит храбрые человек Аника: <br>
  «Я палицу подыму, палицей пришибу, <br>
  Либо коня попущу, конем потопчу<br>
  По главе по твоей». <br>
  Она ему отвечает: <br>
  «Был на земле Самсон-богатырь, <br>
  Был на земле Святогор-богатырь — <br>
  И я их искосила, <br>
  Хочу и тебя, Аника, искосити». <br>
  Возговорит храбрые человек Аника: <br>
  «О смерть, страшна и грозна! <br>
  Дай ты мне сроку хоть на полгода: <br>
  Сострою я тебе церьковь, <br>
  Спишу твой лик на иконе, <br>
  Поставлю твой лик на престоле, <br>
  И будут к нам съезжаться <br>
  Князья и бояре <br>
  И сильные-могучие богатыри, <br>
  И станут нам свозить казну золотую». <br>
  Она ему отвещает: <br>
  «Кабы-де брать мне казну золотую, <br>
  Были бы у меня горы золотые, <br>
  От востока и до запада». <br>
  Вынимает пилы невидимые, <br>
  Потирает его по рукам и по ребрам. <br>
  Возговорит храбрые человек Аника: <br>
  «О смерть, страшна и грозна! <br>
  Дай ты мне сроку на один год. <br>
  Есть у меня казна золотая, <br>
  Раздам я ее по тюрьмам, <br>
  По тюрьмам и по богадельням». <br>
  Она ему отвещает: <br>
  «Казна твоя кровавая — <br>
  Душе твоей не помога». <br>
  Вынимает пилы невидимые, <br>
  Потирает его по костям и по жилам. <br>
  Возговорит храбрые человек Аника: <br>
  «Смерть, страшна и грозна! <br>
  Дай ты мне сроку хоть на полгода. <br>
  Есть у меня отец и мать, <br>
  Дай ты мне с отцом с матерью проститься <br>
  Она ему отвещает:<br>
  «Как же ты ехал на ратное дело,<br>
  Зачем с отцом с матерью не простился?» <br>
  Вынимает пилы невидимые, <br>
  Потирает его по костям, по жилам, <br>
  Аника на коне шатается, <br>
  И смертные уста запекаются, <br>
  Аника с коня повалился, <br>
  Тут по Анике и слава. <br>

