        <br>
		<br>
        …Когда Игнат привел на Майноз свой народ, он договор написал с салтаном, 
        заветы нам дал. В «Книге Игната» сказано: - чтобы церквы не закрывались 
        в Турции; чтобы звон не запрещался; чтобы с турками мы не соединялись; 
        чтобы мы не женились на мусульманках; чтобы салтан против нашей воли казакох 
        на службу не брал; чтобы на войне с Расеей мы в своих не стреляли, а палили 
        через головы. <br>
        <br>
        Сказано в книге, чтоб в Расею при царизме не возвращались, царизме не 
        покорялись; да чтоб держались друг за дружку, без разрешения круга, атамана 
        не уходили из станицы; чтобы по одному никуда не отлучались из Войска; 
        чтоб казак на казака не работал; чтобы каждый рукомесло имел, трудился; 
        чтобы молодые почитали старших; чтобы отдавали третью часть заработка 
        в казну Войска; тайно помогали бедным, явно помогать должон круг; казаки 
        должны любить жен, не обижать их; женщина – мать, ее защищает круг; чтобы 
        попох от Никона не принимали на службу; попа, не сполняющего заветы, можно 
        убить, как еретика; шинкох в станице не держать; чтобы казаки лавок не 
        держали, купцами не были… <br>
        <br>
        <em>Записано Федором Тумилевичем в хуторе Ново-Некрасовском 4 июля 1940 
        г. от Шашкина Семена Фотиевича (1847-1947, род. на Майносе, в России с 
        1912 г.), бывш. атамана Войска Кубанского.<br>
