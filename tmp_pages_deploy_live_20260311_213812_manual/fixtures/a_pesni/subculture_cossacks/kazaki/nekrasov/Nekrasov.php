        Фонд песен о Некрасове и некрасовцах начал складываться во время булавинского 
        восстания (1707-1709) и сидения некрасовцев на Кубани (1708-1741), когда 
        связи с русской средой - прежде всего, с Доном, - еще не были разорваны. 
        После ухода некрасовцев в турецкие земли эта часть песенного наследия 
        в России продолжала самостоятельно бытовать и развиваться. Были также 
        созданы новые песни о мятежном атамане. Всего известно более 80 песен 
        о Некрасове, собранных вне некрасовской среды: на Дону, в Оренбурге, на 
        Урале, Тереке, в неказачьих районах - в Саратовской, Тульской губерниях. 
        Среди них встречаются антинекрасовские версии: Некрасов там предстает 
        &quot;вором-изменником&quot;, спаивает &quot;глупых малолеточек&quot; 
        и уводит на Кубань, крадет &quot;царское знамя&quot; и т. д. Антинекрасовские 
        переработки восходят к тому же периоду, что и первоначальные положительные 
        варианты - это &quot;точка зрения&quot; или &quot;ответ&quot; противников 
        Некрасова, &quot;домовитых&quot; казаков.<em><br>
        </em><br>
        Все песни сводятся к 10 сюжетам на 4 темы - т. е. всего существует 10 
        оригинальных песен, известных в разных вариантах. Большинство сюжетов 
        связано с темой ухода на Кубань - моментом, когда Некрасов шагнул из донской 
        истории в легенду. Большая часть вариантов не опубликована, поэтому поместить 
        их не представляется возможным.<br>
