        <div align="center"><span class="header"><strong>ПЕСНИ НЕКРАСОВЦЕВ</strong><br></span></div>
        <br>
		<br>
        Сбором и изданием фольклорного наследия некрасовцев занимался с 1938 года 
        Федор Викторович Тумилевич (1910-1980), преподававший на филфаках Ростовского 
        университета и Ростовского пединститута. К 1961 году им с женой Тамарой 
        Ивановной было собрано 2190 песенных текстов, из них 150 - в нотной записи 
        и 348 - на магнитофоне, издан 421 текст песен и 22 песни с напевом.<br>
        <br>
        Сами некрасовцы выделяют следующие виды песен: 1) &quot;об Игнате&quot;, 
        2) &quot;под Игната&quot; (политическая лирика: песни разных жанров, впитавшие 
        некрасовское мировосприятие - о чужбине и т. п.), 3) служивые, 4) беседные 
        или компанейские (былины, баллады, жестокий романс, исторические песни 
        не некрасовского цикла), 5) женские, 6) свадебные, 7) курагодные (хороводные), 
        8) крыловые (плясовые), 9) сердцебитные (шуточные), 10) причитания (похоронные, 
        свадебные), 11) пригудки (колыбельные), 12) игровые, 13) считалки, 14) 
        прибаутки-скороговорки, 15) смешные (пародии на духовные стишки), 16) 
        духовные стишки, 17) уличные (сатира на провинившихся членов общины).<br>
