        <br>
        <br>
        Казаки-некрасовцы – потомки донских казаков, участников восстания Кондратия 
        Булавина 1707-1709 годов, которые после гибели Булавина (7 июля 1708 
        года) и поражения главных сил повстанцев отказались сложить оружие и 24-25 
        августа 1708 года ушли во главе с атаманом Игнатом Федоровичем Некрасовым 
        с Дона на Кубань. Согласно преданию, с Некрасовым ушло на Кубань «сорок 
        тысяч казаков» в «7217 году» (т.е. в 1709 году по нашему летоисчислению). 
        В действительности, по оценкам современных историков, ушло 18 тысяч казаков 
        призывного возраста, а если считать вместе с семьями - до 80 тысяч. На 
        Кубани, объединившись с бежавшими туда в 1688 году старообрядцами Левки 
        Манацкого, некрасовцы создали вольное Войско Кубанское и оборонялись от 
        царских войск до 1741 года: совершали набеги на Дон и южные окраины России, 
        жгли и вешали князей, бояр, &quot;домовитых&quot; казаков, бунтовали народ, 
        принимали беглых, засылали подговорщиков с &quot;прелестными&quot; письмами. 
        Сам Некрасов погиб в бою в 1737 году. <br>
        <br>
        В «лето 7249 года» (1741 год) кубанская общность раскалывается. Коренные 
        некрасовцы - потомки повстанцев под давлением русских войск уходят на 
        Дунай в Добруджу - румынскую землю, находившуюся под властью Турции. В 
        1762 году Кубань покидает часть старообрядцев. После странствий одна их 
        ветвь соединяется с дунайцами, другая растворяется в турецком Черноморье. 
        В 1777 году на Дунай перебираются оставшиеся кубанские старообрядцы. В 
        1775 году дельта Дуная становится последним убежищем вольных запорожцев: 
        рядом с некрасовцами возникает Задунайская Сечь. Вскоре между казаками 
        двух войск начались конфликты из-за угоди, которые привели к междуусобной 
        войне. В 1780-е годы русские заняли Крым, взяли Измаил и вышли к Дунаю, 
        и некрасовцы к 1791 году уходят вглубь Турции на «Еноз и Майноз» (на берег 
        Эгейского моря в устье реки Энос и на озеро Майнос, в 25 км от портового 
        города Бандырма на Мраморном море). В 1828 году эйносцы переселились на 
        Майнос. Те, кто остались на Дунае - старообрядцы и поздние поселенцы, 
        - к середине XIX века утратили старые казачьи традиции. Но Дунай, как 
        ранее Кубань, по-прежнему оставался убежищем для беглецов из России. <br>
        <br>
        Майнос, хотя и сильно поредевший от эпидемий, продолжал хранить донские 
        обычаи XVII века и соблюдать «заветы» Игната Некрасова. Казаки с местным 
        населением не смешивались, земли не пахали, занимались рыбным промыслом. 
        Так продолжалось до середины 1860-х годов. В 1864 году майносцы отказались 
        участвовать в турецких войнах. В ответ Турция увеличила казакам подати, 
        ввела военный налог, заселила земли вокруг Войска мухаджирами - мусульманами 
        с Кавказа, а оставшиеся участки стала продавать казакам. 70 семей Майноса 
        купили себе землю, община раскололась на рыбаков и землевладельцев. Рыбаки 
        были в большинстве, круг находился под их контролем, но экономически они 
        начали попадать в зависимость от землевладельцев. «Заветы» нарушились 
        («казак на казака стал работать»).<br>
        <br>
        В 1867 году 157 семей - стронников «заветов» - ушли с Майноса на остров 
        Мада в Бейшеирском озере. Эта попытка возрождения старинного уклада не 
        удалась. &quot;Подморная&quot; земля Мады оказалась смертельной: вода 
        в озере заражена, густые вечерние туманы разносили смерть. Но казаки, 
        даже после вмешательства русской миссии, отказывались уходить: умирали 
        и отправляли людей искать легендарный &quot;Город Игната&quot;, якобы 
        основанный когда-то давно &quot;правильными&quot; некрасовцами &quot;за 
        Пещаным морем&quot;. Почти все колонисты Мады погибли, не найдя своего 
        Города; некоторые вернулись. К 1912 году в живых на Маде оставалось только 
        8 семей. &quot;Город Игната&quot; искали и майносцы, но также не нашли. 
        На карте его никогда не было. Город жил в самих некрасовцах - как воплощение 
        мечты казаков о вольном и справедливом мире.<br>
        <br>
        К началу XX века надежда найти &quot;Город Игната&quot; умерла. Появилось 
        предание о гибели той легендарной затерянной ветви некрасовцев. В 1912-1913 
        годах 150 семей Майноса постановили вернуться на родину. На Дон их царские 
        власти не пустили, сославшись на нехватку земель, и поселили на Кавказе, 
        в 5 км от станции Нотанеби, в селении Лайтуры, но сохранили традиционные 
        права: старую веру, казачье звание и внутреннее самоуправление, построили 
        дома и выделили по 10 десятин леса на семью. В 1917 году, после Февральской 
        революции, грузинское национальное правительство предложило некрасовцам 
        покинуть край, и казаки отправились на Кубань, в станицу Прочноокопскую 
        Армавирского района. Кубанская Рада отказала им в земле. Молодые некрасовцы 
        ушли сражаться в Красную Армию. <br>
        <br>
        Советская власть дала некрасовцам земли и деньги на строительство, но 
        прежняя казачья форма правления – круг - была уничтожена. Поселенцы основали 
        хутор Ново-Некрасовский в 25 км от станции Ахтарская. Вскоре в Россию 
        начали перебираться липованы с Дуная – в хутор Ново-Покровский (1921) 
        и Потемкинский (1924). В 1947 году в Ейский район были переселены из Румынии 
        еще 20 тысяч дунайских семей. 22 сентября 1962 года из Турции, селения 
        Кёджа-Гель (до 1938 года - Бин-Эвле или Эски-Казаклар, по-некрасовски 
        Майнос) в Россию вернулись последние 215 живших там семей некрасовцев 
        - всего 999 человек (тысячный некрасовец родился уже в пути на пароходе). 
        Другая, небольшая часть майносцев - несколько десятков человек - перебралась 
        к русским староверам в Америку. Майносская община прекратили существование. 
        Но история некрасовцев продолжается.<br>
        <br>
        <br>
        Хотя сам Игнат Некрасов погиб еще на Кубани, фольклорные версии его судьбы 
        различны: есть предания, как он ведет казаков с Кубани на Дунай или с 
        Дуная на Майнос, или уходит и основывает &quot;за Пещаным морем&quot; 
        новый &quot;Город Игната&quot;, куда не могут попасть оставшиеся некрасовцы, 
        или до сих пор бродит по свету в поисках лучшей доли для казаков… Знамя 
        Некрасова, простреленное пулей у основания старообрядческого креста, его 
        евангелие и печать были сданы в НКВД. Судьба &quot;Книги Игната&quot;, 
        содержащей &quot;Заветы&quot;, неизвестна. <br>
