    <br>

	«Еду я мимо селенья большого.<br>
	    Что это, брат, за село.<br>
	    Это живут духоборы-крестьяне,<br>
	    Лучшие земли у них...<br>
	    Вольные люди, - ну, точно дворяне,<br>
	    Податей нет никаких.<br>
	    ***<br>
	    Сами пришли и селились охотой,<br>
	    В рекруты их не берут.....<br>
	    Пень | же у нас, не утес, так болото,<br>
	    Где и распашешься тут...<br>
	    ***<br>
	    Нас на Амур то ведь силой селили.<br>
	    Сунут в болото и скажут: - «село».<br>
	    Не до работы. На службах мы гнили.<br>
	    Как не пропали давно.<br>
	    ___</p>
	  <p>«Стой, брат, ямщик. Уж ночка настала,<br>
	    Вьюга завыла, шумит.<br>
	    Видишь ты хату, в селе у начала,<br>
	    Ту, что там с краю стоит.<br>
	    ***<br>
	    Трогай туда, постучи-ка в окошко,<br>
	    Старая дома - поди.<br>
	    Надо заехать... закусим немножко<br>
	    И отдохнем до зари...<br>
	    ***<br>
	    Вышла старуха и смотрит уныло,<br>
	    В сумраке, холод ночной:<br>
	    Видно начальник... давно их не было...<br>
	    Надо принять на постой.<br>
	    «Здравствуйте, барин! Эх вьюга какая, -<br>
	    И не узнала-б никак.....<br>
	    Ну, заходите..... хоть хата малая -<br>
	    Все-ж разместимся кой-как».<br>
	    ***<br>
	    Холодно в хате, чуть лампа мелькает,<br>
	    Пусто кругом по стенам.....<br>
	    Села старуха и стонет, вздыхает,<br>
	    Все-то печалится нам:<br>
	    ***<br>
	    «Старая я уж, из сил выбиваюсь,<br>
	    Некому дров нарубить.....<br>
	    День-то денской все работаю, маюсь...<br>
	    Да уж не долго мне жить.....<br>
	    ***<br>
	    Третьего сына на службу угнали...<br>
	    Всех отобрали, осталась ни с чем,<br>
	    Сроду беды мы такой не знавали...<br>
	    Осиротела совсем.<br>
	    Хлеб продала я последний и сбрую...<br>
	    Справила форму ему...<br>
	    Лошадь взяла я у кума гнедую -<br>
	    За долг работать пойду.<br>
	    ***<br>
	    Продала все я, что было в амбаре.<br>
	    Справила кой-как седло...<br>
	    Всех-то овец извела на базаре...<br>
	    И разоренье пошло.<br>
