        Уникальность песни состоит в том, что она присутствовала во всех без исключения 
        экспедициях, с репертуаром которых составителям довелось познакомиться. 
        Вторая ее особенность состоит в том, что у этой песни практически нет 
        вариантов — имеются два независимых, несводимых один к другому текста, 
        причем в причерноморских экспедициях преобладает один из них, а в северо-западных 
        — другой. И, наконец, третья особенность песни: ее ни разу не удалось 
        зафиксировать за пределами археологической среды: если такие фиксации 
        и были, то каждый раз оказывалось, что информант узнал песню от знакомого, 
        бывавшего в археологических экспедициях. Публикуемый текст &quot;южного 
        варианта&quot; записан в 1994 г. в Мирмекийской экспедиции ИИМК РАН (нач. 
        экспедиции Ю. А. Виноградов), &quot;северный&quot; вариант публикуется 
        по списку из рукописного сборника В. И. Мордвинцевой, составленного в 
        1986—1987 гг. (дополнялся до начала 90-х гг.) в экспедициях Волгоградского 
        университета.</em><br>
