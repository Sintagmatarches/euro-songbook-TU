        <br>
        Проклиная судьбину свою<br>
        (На дворе, между прочим, январь!)<br>
        Он нарочно наполз на змею,<br>
        И впилась ядовитая тварь!<br>
        В чай разбитое бросил стекло,<br>
        Глядя все на заветный платок,<br>
        И, чтоб время не даром прошло,<br>
        Сделал очень приличный глоток.<br>
        <br>
        Фудзияма — не яма — гора...<br>
        <br>
