        Из знатных дворянских кровей,<br>
        Ее называли Карениной Анной,<br>
        Самойлова – пшик перед ней.<br>
        <br>
        А Вронский, замечу, был парень-пройдоха,<br>
        К тому же еще офицер.<br>
        Его воспитала другая эпоха,<br>
        А не комсомол СССР.<br>
        <br>
        Когда он упал на своем ипподроме<br>
        И лошадь с горячки убил,<br>
        За этот бы случай при нашем месткоме<br>
        Он суток пятнадцать схватил.<br>
