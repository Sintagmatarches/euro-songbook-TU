        Согласно мемуарам искусствоведа Бориса Алмазова, песня (он называет ее 
        &quot;Вас просит Каренин Сергей...&quot;) написана для капустника послевоенным 
        поколением студентов ленинградского Театрального института (ул. Моховая, 
        28). Представленный выше вариант - поздний, брежневских времен: в нем 
        упоминается актриса Татьяна Самойлова, а фильм &quot;Анна Каренина&quot; с Самойловой 
        в главной роли вышел на экраны в 1967 году.<br>
        <br>
        В том же институте, как пишет Алмазов, была написана <a href="slvvatikane.php">&quot;Этот 
        случай был в городе Риме...&quot;</a> Кроме того, студенты театроведческого 
        факультета Театрального института профессионально занимались созданием 
        песен для певцов-попрошаек. Ими было написано множество песен - некоторые 
        бытуют до сих пор, и даже узнаваемы по подчеркнутой &quot;театральности&quot;, 
        но при этом так и остались анонимными: анонимность автора была обязательным 
        условием сделки. (См.: Б. Алмазов. Не только музыка к словам… М.: Центрполиграф, 
        2003, стр. 307-315). <br><br>
