        <strong>Рецепт Крамбамбули</strong>, из поваренной книги «Денщик за повара», 
        1914 год:<br>
        <br>
        Ингредиенты:<br>
        <br>
        Шампанское – 2 бутылки<br>
        Ром – 1 бутылка<br>
        Сотерн (белое десертное вино) – 1 бутылка<br>
        Сахар – 1кг<br>
        Ананас – 1 шт.<br>
        Большой кусок сахара – 1<br>
        <strong><br>
        </strong><strong>Способ приготовления:</strong><br>
        <br>
        В кастрюлю влить две бутылки шампанского, одну бутылку лучшего рома и 
        одну бутылку хорошего сотерна (белого десертного вина), положить килограмм 
        сахара, порезанный ананас, и вскипятить на плите; вылить в фарфоровую 
        вазу, наложить на края крестообразно две вилки или шпаги, на них большой 
        кусок сахару, полить его ромом, зажечь и подливать ром, чтобы весь сахар 
        воспламенился и растаял, брать серебряной суповой ложкой жженку, поливать 
        сахар, чтобы огонь не прекращался, прибавляя свежего рома, а между тем 
        разливать в ковшики или кубки.<br>
