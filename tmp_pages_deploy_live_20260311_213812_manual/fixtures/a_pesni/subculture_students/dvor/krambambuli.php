        <strong>Крамбамбули</strong> – вид пунша, приготавливаемый обычно из вина, 
        рома, сахара, фруктового сока и/или фруктов, а также пряностей. Название 
        обычно возводят к крепкому спиртному напитку производившемуся на ликёрной 
        фабрике Isaak Wedel-Links und Eydam Dirck Hekker в городе Данциг (ныне 
        город Гданьск в Польше). Само слово было образовано путём слияния слов 
        Krandewitt (одно из названий можжевельника) и Blamp, на жаргоне означавшее 
        алкогольный напиток. Данный напиток с разной рецептурой выпускается и 
        сейчас в Польше (концерн SOBIESKI) и Белоруссии (компания БЕЛПИ).<br>
        <br>
        Тем не менее, наибольшую популярность получил именно пунш, который в России 
        зачастую именовали жжёнкой. Большими поклонниками крамбамбули – жжёнки 
        были поэты Н.М. Языков и А.С. Пушкин, а также Гоголь, который очень любил 
        готовить жжёнку для своих друзей. Ещё большей популярностью крамбамбули 
        или жжёнка пользовались в среде студентов и офицеров. Свои рецепты были 
        у каждого полка и каждой студенческой организации. Приготовление крамбабули 
        и жжёнки сопровождалось целым ритуалом. Так гусары 10-го гусарского Ингерманландского 
        полка пели песню «Где гусары прежних лет» Дениса Давыдова, а в студенческих 
        корпорациях пелась песня на слова Кристофа Ведекинда, которая в своих 
        экстремальных вариантах достигала длины в 106 (sic!) куплетов. Именно 
        эта песня и была переведена на множество языков, в том числе и русский. 
        Перевёл песню поэт Н.М. Языков, член русской студенческой корпорации &quot;Ruthenia&quot; 
        в Дерпте (ныне город Тарту, Эстония).<br>
