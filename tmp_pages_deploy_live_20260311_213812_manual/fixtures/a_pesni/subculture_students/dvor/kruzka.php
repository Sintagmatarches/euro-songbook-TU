  Эх, подружка,<br>
  Моя большая кружка,<br>
  Полулитровая моя!<br>
  <br>
  Иду на завтрак, на обед или на ужин,<br>
  И ты со мною на брезентовом ремне.<br>
  И как солдату автомат в атаке нужен,<br>
  Так и в столовой ты необходима мне.<br>
  <br>
  Придет пора, и мы расстанемся с тобою,<br>
  Из лагерей мы возвратимся по домам.<br>
  Я подниму тебя дрожащею рукою,<br>
  Налив в тебя свои прощальные сто грамм.<br>
  <br>
  1953<br>
  <br>
  <br>
        Песни &quot;Кружка&quot; и <a href="../army/otgremeli.php">&quot;Отшумели весенние 
        грозы&quot;</a> (вторая - на мотив <a href="../drugije/proschanie.php">&quot;Прощания 
        славянки&quot;</a>) написаны студентом Ленинградского института инженеров железнодорожного транспорта (ЛИИЖТ) Анатолием Чеповецким, братом детского писателя 
        и поэта Ефима Чеповецкого (автора сценария и текстов песен мультфильма 
        &quot;Приключения капитана Врунгеля&quot;), в июле 1953 г. во время прохождения 
        лагерных сборов на ст. Горьковская под Ленинградом в качестве взводной 
        и ротной песен к предстоящему строевому смотру части. В августе того же года однокурсник Анатолия Чеповецкого Марк Лабок в альпинистском лагере &quot;Локомотив&quot; в кавказском ущелье Адыл-Су переделал курсантский вариант в альпинистский.<br>
  <br>
        Приведенный выше текст как первоначальный прислан сыном автора Михаилом 
        Чеповецким &lt;chepm @ eathlink.net&gt; 29 июня 2009 г., со ссылкой 
        на отца:<br>
  <br>
  <em>&quot;Обе эти песни были написаны летом 1953 года во время прохождения лагерных 
  сборов на ст. Горьковская под С.-Петербургом (Ленинградом) в качестве взводной 
  и ротной песен к предстоящему строевому смотру части. Позднее в альпинистском 
  лагере последние две строчки второго куплета были изменены:<br>
  <br>
  Как альпинисту ледоруб в походе нужен,<br>
  Так и в столовой ты необходима мне. <br>
  <br>
  Анатолий Чеповецкий<br>
  Иерусалим, Израиль, 28.06.09<br>
  achepovetsky @ gmail.com&quot;</em><br>
  <br>
  <br>
        Ранее, 13 марта 2009 г., Михаил прислал следующее:<br>
  <br>
  <em>&quot;Песня &quot;Кружка&quot; - не военного времени, она написана в начале 
  50-х. Автор ее, так же как и <a href="../army/otgremeli.php">&quot;Прощания славянки&quot;</a> 
  - мой отец, Анатолий Чеповецкий. &quot;Кружка&quot; была написана на Кавказе, 
  в альпинистких лагерях, а &quot;Славянка&quot; - на военных сборах, в 53 году. 
  Отец мой жив и живы еще свидетели того, как эти песни появились на свет. Хотя, 
  увы, и мало их осталось. <br>
  <br>
  В отличие от своего брата, Ефима Чеповецкого, автора многих детских книг (&quot;Пешкин&quot;, 
  &quot;Непоседа, Мякиш и Нетак&quot; и другие) и песен (&quot;Мы бандито...&quot; 
  из мультфильма &quot;Капитан Врунгель&quot;), мой отец профессиональным поэтом 
  не стал, хотя композитор Матвей Блантер приглашал его с ним сотрудничать&quot;. 
  <br>
  <br>
  P.S. Если Вам интересно, у меня есть написанные папой полные &quot;правильные&quot; 
  тексты &quot;Кружки&quot; и &quot;Славянки&quot;.<br>
  </em>
  <br>  <br>
  Воспоминание Марка Лабка, присланы автором 20 июня 2016 из Торонто:<br>
  <br>  
  <em>Случайно "наткнулся" на песню "Кружка" моего однокурсника из ЛИИЖТ'а Толика
Чеповецкого, написанную им в июле 1953 года во время нашей военной практики.
Вношу ясность в вопрос об альпинистской кружке:  <br>    <br>  
Две строчки в курсантскую версию песни, превратившую её в альпинистский гимн
середины 50-х годов, за 14 лет до песен Владимира Высоцкого из к/ф "Вертикаль",
сочинил я, сразу после военных сборов, в августе 1943 года в альплагере"Локомотив",
в ущелье Адыл-Су на Кавказе, которую тут же запел весь лагерь.  <br>  
  <br>  
Так как тогда новых альпинистских песен не было, то этот парафраз песни "Кружка"
Анатолия Чеповецкого подхватили студенты альпинисты-железнодорожники и разнесли
по всей стране (ко мне в Ленинград приходили просьбы выслать слова песни,
в частности, из города Анжеро-Судженска, о котором я и не слышал). <br>
  </em><br>
