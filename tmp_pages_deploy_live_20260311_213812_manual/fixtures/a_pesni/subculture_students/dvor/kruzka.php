  Слова Анатолия Чеповецкого<br>
  <br>
  Прошла от дома ты лагерного сбора,<br>
  Судьба свела нас за обеденным столом.<br>
  И мы с тобой уже почти неделю скоро<br>
  Как неразлучные товарищи живем.<br>
