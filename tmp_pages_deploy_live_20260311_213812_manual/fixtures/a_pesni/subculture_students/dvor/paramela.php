  <br>
  Песня "Парамелла" считается цыганской народной. На самом деле она такая же "цыганская", как "Уно моменто" - "неаполитанская". Нет такой цыганской песни, и настоящие цыгане ее не поют.
  А есть цыганская песня "Звездочка", написанная цыганкой Орловой в 1931 году специально для исполнения своеобразной и редкой певицей Софьей Николаевной Лебедевой и изданной на пластинке в 1939 году.
  Вот эту "Звездочку" немного переделали, а вместо традиционного припева "Ай-нэ-нэ-нэ-нэ..." прицепили имитацию цыганского языка "Я парамелла, я чебурелла…".<br>
  <br>
  
  В юности мне довелось якшаться с цыганами и даже целый месяц "чесать" с ними один район. Когда я спрашивал &quot;Почему Парамеллу не исполняем?&quot;, мне ответили: &quot;Не цыганская песня, нет у нас таких слов&quot;.<br>
  <br>
  Есть похожее "Ёв парувэла, ай чявуралэ!" (типа он меняет, ай ребята). В общем, предлагать цыганам петь &quot;Парамеллу&quot; - все равно, что итальянцам предлагать "Уно моменто".<br>
  <br>
  Пусть ее поют русские и псевдо-цыгане. Вот у них забойная "Парамелла" популярна.<br>
  <br>
  У этой песни есть разные варианты слов. В том числе "Батька лошадь запрягает ...", "Запрягай-ка батька лошадь...".<br><br>
  Так "Батька лошадь запрягает" написал Булат Окуджава! Это была стилизация под фольклор, но на мелодию "Звездочки". Эта запись у меня есть.<br><br>
  А потом эту песню стали исполнять, наверное, все казачьи хоры и ансамбли. А потом она превратилась в псевдо-цыганщину "Парамеллу".<br>
  <br>
  Тем не менее эта песня многими любима. Мы ее исполняли и ансамблем нашего стройотряда для публики и просто орали, разъезжая ночами по деревням на двух ЗИЛ-ах.<br>
  <br>
  И в армии орали, проезжая ночью Свердловск, и даже она, с переделанными словами, была у нас строевой. <br>
