  Обычно называется по припеву – &quot;Я парамела&quot; или просто &quot;Парамела&quot;. В основе - мелодия песни &quot;Звездочка&quot; из репертуара исполнительниц цыганского романса 1930-х гг. и стихотворение Булата Окуджавы &quot;Запрягай-ка, батька, лошадь&quot; о сватовстве к казачке. &quot;Звездочку&quot; на пластинку записывали Тамара Церетели (Москва, 1939, Апрелевский и Ногинский заводы, <a href="https://russian-records.com/details.php?image_id=7850" target="_blank">№8409</a>) и Екатерина Юровская (Ленинград, 1939, Ленмузтрест, <a href="https://russian-records.com/details.php?image_id=20232" target="_blank">442A</a>). В &quot;Звездочке&quot; русский текст чередуется с цыганским, в &quot;Парамеле&quot; - с псевдо-цыганским.  Вероятный автор &quot;Парамелы&quot; - руководитель группы &quot;Магаданцы&quot; Анатолий Мезенцев; песня вошла в альбом группы 1972-73 гг.<br>
