	<span class="header"><strong>ПЕРЕПЕТУЯ 
</strong> <br></span>
<br>
Я стою у заросшего сада,<br>
У меня гитара под полой…<br>
Я спою тебе серенаду, дорогая!<br>
Хочешь слушай, а хошь иди домой.<br>
<br>
<em>Припев:</em><br>
<br>
Так поцелуй же ты меня, Перепетуя,<br>
В кончик носа!<br>
Я тебя так безумно люблю,<br>
Так люблю!<br>
Для тебя чем угодно рискуя,<br>
Кроме денег,<br>
Спекульну, спекульну, спекульну!<br>
<br>
Всю кровать твою завешаю я воблой,<br>
Чтоб спокоен и сладок был твой сон.<br>
А если воблы да я не достану,<br>
Пригоню я узюма вагон.<br>
<br>
Я куплю тебе розового мыла –<br>
Хочешь, мойся, а хочешь им торгуй, -<br>
Но чтобы крепче меня ты любила, дорогая,<br>
Чтобы слашше был твой поцелуй!<br>
<br>
Чудный сад разведу я на помойке,<br>
В том саду будет петь крокодил…<br>
Приходи же ко мне, моя Перепетуя,<br>
Не гнушайся обчеством моим!<br>
<br>
<em>В нашу гавань заходили корабли. Вып. 3. М.: Стрекоза, 2000.</em><br>
<br>
<br>
Песня появилась не позднее начала 1940-х. Вполне возможно, во времена НЭПа. <em>&quot;У меня гитара под полой…&quot;</em> - отсылка к популярной студенческой песне XIX века <a href="../popular20/nakinuplach.htm">&quot;Накинув плащ, с гитарой под полою&quot;</a> на слова Владимира Соллогуба. Есть <a href="../army/perepet-arm.php">солдатские версии</a>.<br>
<br>
Вариант окончания припева: <span class='i'>&quot;Для тебя чем угодно рискуя, /Обману, украду, шухерну&quot;</i> (прислал Vadim Rapp vrapp @ polyscience.com
   14 сент. 2019).<br>
