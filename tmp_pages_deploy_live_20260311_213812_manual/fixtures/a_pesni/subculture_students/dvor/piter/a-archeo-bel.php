        <br>
        <strong><br>
        ПЕСНИ ПСКОВСКОЙ ЭКСПЕДИЦИИ ЭРМИТАЖА</strong><br>
        <br>
        В 60-е годы Псковская экспедиция Эрмитажа была &quot;поющей&quot;, хотя 
        пели и не слишком часто: сказывалась специфика городских раскопок - отсутствие 
        такого важного элемента экспедиционной жизни, как &quot;вечера у костра&quot;. 
        Пели в эрмитажном автобусе по пути из Питера в Псков и обратно, пели на 
        привальной-отвальной и &quot;в дни рождения&quot;, много пели 15 августа. 
        Гитаристов не было, поэтому пели &quot;а капелла&quot;. Отмечу особенность 
        экспедиции 60-х годов: крайне редкое употребление горячительных напитков. 
        Это, разумеется, не означает, что в экспедиции был сухой закон, однако 
        алкоголь в застольях бывал не часто и в ограниченных количествах. Может 
        быть, поэтому в 60-е годы &quot;питейный&quot; цикл песен не был развернутым.<br>
        <br>
        Пели &quot;народные&quot; и &quot;военные&quot; песни (в том числе - песня 
        &quot;Забьем козла, браточки&quot;, которую я больше нигде не слышал). 
        Из так называемых &quot;лагерных&quot; песен помню &quot;Таганку&quot;, 
        &quot;Ванинский порт&quot;, &quot;Сухарики&quot;, из студенческой классики 
        - &quot;Там, где Крюков канал&quot; (<a href="a-archeo-bv.php#3" target="_blank">№ 
        3</a>) , &quot;По рюмочке, по маленькой&quot; (<a href="a-archeo-bv.php#2" target="_blank">№ 
        2</a>) и &quot;В скучные минуты Бог создал институты&quot; (<a href="a-archeo-bv.php#5" target="_blank">№ 
        5</a>). Пели также студенческие гимны - &quot;Раскинулось поле по модулю 
        пять&quot; и &quot;Тот, кто физиком стал&quot;.<br>
        <br>
        Среди песен, устойчиво ассоциирующихся с началом 60-х, были &quot;Бабка 
        Любка&quot;, &quot;Чемоданчик&quot;, &quot;Двадцать метров крепдешина&quot;, 
        &quot;Кровать со фигурными шишками&quot;, &quot;Расцвела сирень в моем 
        садочке&quot;, &quot;Жора, подержи мой макинтош&quot;, &quot;Четырнадцать 
        французских моряков&quot;, &quot;У девушки с острова Пасхи&quot;, &quot;Есть 
        в Батавии маленький дом&quot;, а также песни, привезенные В. Д. Белецким 
        в Псков вместе с &quot;саркельскими песнями&quot; (&quot;Гипопотам&quot;, 
        &quot;Перепетуя&quot;, &quot;Сюзанна&quot;, &quot;На острове Таити&quot;). 
        Конечно, пели &quot;Бригантину&quot; Павла Когана. Думаю, что пелись многие 
        из этих песен и раньше (по крайней мере с конца 50-х), хотя уверенно утверждать 
        не берусь. Часто пели &quot;В пещере каменной&quot; (на мелодию &quot;Арии 
        варяжского гостя&quot;), причем привычный мне текст и рефрен заметно отличаются 
        от вариантов этой песни, певшихся в застольях 90-х и даже 80-х годов (ср.: 
        <a href="a-archeo-bv.php#8" target="_blank">№ 8</a>):<br>
        <br>
        <strong>В пещере каменной</strong><br>
        <br>
        В пещере каменной нашли наперсток водки,<br>
        Комар зажаренный валялся на песке.<br>
        <br>
        Ели. Пили. Мало.<br>
        <br>
        В пещере каменной нашли чекушку водки,<br>
        Цыпленок жареный валялся на песке.<br>
        <br>
        Ели. Пили. Мало.<br>
        <br>
        В пещере каменной нашли пол-литру водки,<br>
        И целый окорок валялся на песке.<br>
        <br>
        Ели. Пили. Мало.<br>
        <br>
        В пещере каменной нашли боченок водки,<br>
        Барашек жареный валялся на песке.<br>
        <br>
        Ели. Пили. Мало.<br>
        <br>
        В пещере каменной нашли цистерну водки, <br>
        И слон зажаренный валялся на песке.<br>
        <br>
        Ели. Пили. Мало.<br>
        <br>
        В пещере каменной нашли источник водки,<br>
        И стадо мамонтов паслось невдалеке.<br>
        <br>
        Ели. Пили. Норма.<br>
        <br>
        Не часто, но пели &quot;Мадагаскар&quot;. Сейчас я уже не могу припомнить 
        текст, который пелся в 60-е годы, поэтому воспроизвожу его в том виде, 
        в котором эту песню пели в экспедиции позднее - в конце 70-х и в 80-е 
        годы:<br>
        <br>
        <strong>Мадагаскар</strong><br>
        <br>
        Чутко горы спят, Южный крест залез на небо,<br>
        Спустились с гор в долины облака.<br>
        Осторожней, друг, ведь никто из нас здесь не был -<br>
        В таинственной стране Мадагаскар.<br>
        <br>
        Наш бесшумный шаг, чуть колышутся лианы.<br>
        Ведь мы со всех сторон окружены.<br>
        Осторожней, друг, даль подернулась туманом -<br>
        Сними с плеча свой верный карабин.<br>
        <br>
        Мы спустились с гор и вошли с тобой в таверну,<br>
        Где старый негр разливал коньяк.<br>
        Осторожней, друг, ведь порой бывает скверно<br>
        Тому вдвойне, кто выпить не дурак.<br>
        <br>
        Помнишь южный порт, и накрашенные губы,<br>
        И купленный за доллар поцелуй.<br>
        Осторожней, друг, эти ласки слишком грубы -<br>
        Беги от них и сердце не волнуй.<br>
        <br>
        Южный крест погас, в золотых потоках небо,<br>
        И скоро солнце встанет из-за скал.<br>
        Осторожней, друг, тяжелы и метки стрелы<br>
        У жителей страны Мадагаскар.<br>
        <br>
        &quot;Фирменной&quot; экспедиционной песней долгое время (по крайней мере, 
        до начала 70-х) была &quot;Разлука&quot; с текстом, который я за пределами 
        экспедиции не встречал.<br>
        <br>
        <strong>Разлука</strong><br>
        <br>
        Пропойте мне разлуку,<br>
        Мне милый изменил.<br>
        Не понял мою муку,<br>
        Другую навек полюбил.<br>
        <br>
        Приходит ко мне милый,<br>
        Накручиват усы,<br>
        Сымает мягку шляпу,<br>
        Сам смотрит на с'гирям часы.<br>
        <br>
        Смотри, смотри изменщик,<br>
        Смотри который час.<br>
        К сопернице счастливой<br>
        Идешь ты в последний раз.<br>
        <br>
        Пойду, пойду в аптеку,<br>
        Куплю я кислоты.<br>
        Лишу тебя навеки<br>
        Небесной твоей красоты.<br>
        <br>
        Сама же я с балкону<br>
        Вниз брошусь головой<br>
        И буду белой пеной<br>
        На пыльной лежать мостовой.<br>
        <br>
        Ах, чья енто могилка<br>
        Травою поросла?<br>
        То Дунька, Ванькина милка<br>
        Любовью своей изошла.<br>
        <br>
        В 70-е годы на эту же мелодию пелась другая песня - &quot;Шестнадцать 
        столовых ножей&quot;, постепенно вытеснившая из репертуара традиционную 
        &quot;Разлуку&quot;.<br>
        <br>
        <strong>Шестнадцать столовых ножей</strong><br>
        <br>
        Прошло уж две недели,<br>
        Сказал миленок так:<br>
        &quot;Неужто в самом деле<br>
        Рассчитываете на брак?&quot;<br>
        <br>
        Маруська тут решила,<br>
        Что стала жизнь хужей,<br>
        И в грудь себе вонзила - втыкнула -<br>
        Шестнадцать столовых ножей.<br>
        <br>
        Мотор колеса крутит,<br>
        В разлет летит Москва.<br>
        Маруська в институте<br>
        Сиклифасовскова.<br>
        <br>
        К Маруське подбегают<br>
        Шестнадцать докторей,<br>
        И кажный врач вынает<br>
        Ей ножик из белых грудей.<br>
        <br>
        Один остался ножик,<br>
        Один остался врач,<br>
        И вдруг со стола раздался<br>
        Маруськин жалобный плач:<br>
        <br>
        &quot;Не лапайте руками, нахалы,<br>
        Довольна я вполне.<br>
        Шестнадцатый ножик на память<br>
        Пущай остается во мне&quot;.<br>
        <br>
        Маруську в крематорий -<br>
        И сразу в печь суют.<br>
        В тоске и в страшном горе<br>
        Миленок ее тут как тут: <br>
        <br>
        &quot;Ах, я ей жизнь испортил,<br>
        И виноват я сам.<br>
        Насыпьте пожалуйста, в портфель<br>
        Мне пеплу четыреста грамм&quot;.<br>
        <br>
        Ах, чья енто могилка<br>
        Травою поросла?<br>
        Маруська, Ванькина милка<br>
        Любовью своей изошла.<br>
        <br>
        Я знаю - город будет,<br>
        Я знаю - саду цвесть,<br>
        Когда такие люди, как Ванька с Маруськой,<br>
        В деревне нашей есть.<br>
        <br>
        Дольше всего из песен начала 60-х в репертуаре экспедиционных посиделок 
        продержалась &quot;На станции нашей…&quot; (на мелодию песни &quot;Крутится, 
        вертится шар голубой&quot; из кинофильма &quot;Юность Максима&quot;); 
        с этой песней я вне Псковской экспедиции Эрмитажа не сталкивался. Правда, 
        в 80-е годы она уже воспринимались как &quot;древность&quot; и в застолье 
        15 августа была традиционной &quot;сольной партией начальника&quot;. <br>
        <br>
        <strong>На станции нашей…</strong><br>
        <br>
        На станции нашей перрон и вокзал.<br>
        На станции нашей любви я не знал.<br>
        На станции нашей туда и сюда<br>
        Проходят составы, идут поезда.<br>
        <br>
        Однажды на станцию вечерней порой,<br>
        На станцию прибыл экспресс голубой,<br>
        И вот из вагона под номером пять<br>
        Транзитная барышня вышла гулять.<br>
        <br>
        И с этого часа мой сон и покой<br>
        Куда-то умчал тот экспресс голубой.<br>
        И вижу я чудной сирени букет,<br>
        Белую кофточку, синий берет.<br>
        <br>
        Где эта барышня, где мой покой,<br>
        Куда ты умчался экспресс голубой?<br>
        Никто не ответит где барышня та -<br>
        Ентая барышня инкогнита.<br>
        <br>
        В начале 60-х на мелодии популярной &quot;музыки на костях&quot; пели 
        также песни вроде &quot;Песенки про Лумумбу&quot;, которую я с тех пор 
        больше нигде не слышал:<br>
        <br>
        <strong>Песенка про Лумумбу</strong><br>
        <br>
        Есть в Африке Конго,<br>
        Есть в Африке Конго,<br>
        Есть в Африке Конго такая страна.<br>
        <br>
        Есть в Конго Катанга,<br>
        Есть в Конго Катанга,<br>
        Есть в Конго Катанга провинция.<br>
        <br>
        Жил в Конго Лумумба,<br>
        Жил в Конго Лумумба,<br>
        В Катанге Лумумба, правителем был.<br>
        <br>
        Но злобный Мобута,<br>
        Но злобный Мобута,<br>
        Но злобный Мобута Лумумбу убил.<br>
        <br>
        Убили Лумумбу,<br>
        Убили Лумумбу,<br>
        Сожрали Лумумбу - осталась берцовая кость.<br>
        <br>
        О хам-хам-хам-хам-хам, о шейк!<br>
        Где ты был? - В Конго был!<br>
        - Что ж ты Мобуту упустил!<br>
        <br>
        С середины 60-х в песенный репертуар экспедиционных посиделок вошли &quot;Ёксель-моксель&quot;, 
        &quot;Я рязанский уроженец&quot;, &quot;Цилиндром на солнце сверкая&quot;, 
        &quot;Я чешу, чешу ногу&quot;, некоторые туристские песни (&quot;Долой, 
        долой туристов&quot;, &quot;Какао&quot;), &quot;Раз в московском кабачке 
        сидели&quot;, &quot;По тундре, по железной дороге&quot;, а также безымянные 
        тогда &quot;На материк&quot;, &quot;Пиратскую&quot; и &quot;Снег&quot; 
        Городницкого, &quot;Домбайский вальс&quot; Визбора. По-моему, тогда же 
        в экспедиции появилась песенка &quot;Он был кавалер-импозанто&quot;, впрочем 
        не уверен - может быть и раньше.<br>
        <strong><br>
        Он был кавалер-импозанто</strong><br>
        <br>
        Он был кавалер-импозанто,<br>
        Служил он в утиль сырьёрито,<br>
        Носил вместо галстука банто<br>
