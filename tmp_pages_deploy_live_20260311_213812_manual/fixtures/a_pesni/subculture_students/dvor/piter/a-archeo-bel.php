        Изборской экспедиции составили профессиональные и полупрофессиональные 
        археологи - студенты и аспиранты начальника экспедиции, а также сотрудники 
        Псковского музея-заповедника и Отдела археологических сводов ИА АН СССР, 
        который в те годы возглавлял В. В. Седов. Большинство лаборантов Изборской 
        экспедиции в эти годы уже имели за плечами десяток и более полевых сезонов 
        в разных экспедиций (Новгородской, Псковской, Верхне-Волжской и др.). 
