        Говорить обо всех песнях Изборской экспедиции ИА АН СССР (теперь - РАН) 
        за почти четверть века ее существования было бы рискованно: я принимал 
        участие в работах этой экспедиции с 1973 по 1980 г., а постоянно приезжал 
        &quot;в гости&quot; из Пскова с 1981 по 1985 г., так что могу судить, 
        прежде всего, о песнях, певшихся именно в эти годы. <br>
        <br>
        Изборская экспедиция середины 70-х годов по составу певшихся песен заметно 
        отличается экспедиции конца 70-х - начала 80-х, и это легко объясняется 
        различиями состава участников. В середине 70-х основная часть сотрудников 
        экспедиции - и землекопов, и лаборантов - состояла из студентов-практикантов 
        Ивановского Государственного университета, причем каждый год состав практикантов 
        менялся. Основным песенным репертуаром в изборских застольях того времени 
        были некоторые песни Высоцкого и &quot;блатные&quot; песни с кассет Аркадия 
        Северного. В ходу были разнообразные &quot;кричалки&quot; типа пионерских 
        речевок 60-х годов, популярные советские песни и песни из мультфильмов, 
        некоторые из которых дописывались .<br>
