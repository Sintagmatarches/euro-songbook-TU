        Песенный фольклор Псковской экспедиции 70-х и 80-х годов характеризовать 
        значительно труднее, поскольку рубеж 60-х - 70-х годов был переломным 
        в археологии Пскова. С 1969 г. в раскопки Пскова, до этого времени проводившиеся 
        только экспедицией Эрмитажа, включилась вновь созданная экспедиция Псковского 
        пединститута (нач. экспедиции И. К. Лабутина), так что практиканты и энтузиасты, 
        приезжавшие в Псков для участия в раскопках, оказывались в составе разных 
        экспедиций. А с середины 70-х годов, когда к охранным раскопкам Пскова 
        стали ежегодно подключаться новые экспедиции и отряды (так сказать &quot;разовые&quot;, 
        создававшиеся, чаще всего, &quot;под конкретный раскоп&quot;), размежевание 
        приобрело практически необратимый характер. И хотя застолья на 15 августа 
        по традиции еще оставались общими, посиделки уже разделились, и быстро 
        сформировались вполне самостоятельные песенные репертуары. Конечно, между 
        наборами песен было много общего: это и какая-то часть &quot;экспедиционной 
        классики&quot;, восходящей к экспедиции Гроздилова 50-х - начала 60-х 
        годов, это и приблизительно одинаковый состав &quot;бардовских песен&quot;, 
        попадавших в экспедицию вместе с приезжающими участниками раскопок. Но 
        почти сразу в разных экспедициях, проводивших раскопки Пскова, начали 
        появляться &quot;свои&quot; песни, состав которых в разных псковских экспедициях 
        различался. Не последнюю роль сыграло и то, что комсостав &quot;разовых 
        экспедиций&quot; приезжал в Псков со своим песенным репертуаром, истоки 
        которого восходили к другим экспедициям - ладожской, новгородской, изборской.<br>
        <br>
        Но был и еще один фактор, повлиявший на изменение песенного репертуара 
        Псковской экспедиции Эрмитажа. Дело в том, что в 70-е годы в экспедиции 
        практически ежегодно стали появляться участники, не только певшие, но 
        и игравшие на гитаре. Именно &quot;человек с гитарой&quot; с этого времени 
