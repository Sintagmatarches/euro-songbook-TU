  <br>
  В каюте класса первого <br>
  Садко, богатый гость, <br>
  Бутылки бьет об голову,<br>
  Срывает на них злость. <br>
  <br>
  - Ох, слуги, слуги верные, <br>
  Послушайте меня! <br>
  Три бочки водки выдули, <br>
  А толку ни хрена. <br>
  <br>
  Ах, братцы вы матросики, <br>
  Ах, мать вашу ети! <br>
  Давайте жребий бросим мы, <br>
  Кому на дно идти! <br>
  <br>
  Предсмертный жребий выброшен, <br>
  И пал он на Садко. <br>
  Со смеху надрывается <br>
  Дружинушка его. <br>
  <br>
  И вот он собирается, <br>
  Берет свой чемодан, <br>
  Кидает туда яйца <br>
  И книжку - Мопассан. <br>
  <br>
  На мачту он взбирается <br>
  И в океан - бултых! <br>
  На дно он опускается, <br>
  И океан затих... <br>
  <br>
  И вот Садко на дне морском, <br>
  Как вкопанный, стоит. <br>
  К нему, виляя жабрами, <br>
  Огромный кит летит. <br>
  <br>
  Садко не растерялся, <br>
  Кита за жабры - хвать! <br>
  - Скажи мне, чудо-рыба, <br>
  Как мне к царю попасть??? <br>
  <br>
  Садись ко мне на спину, <br>
  Но чур - не щекотать... <br>
  А ты за это книжку <br>
  Дозволь мне почитать. <br>
  <br>
  Садко с царем сдружился, <br>
  Морскую дочь пленил, <br>
  И я на ихней свадьбе <br>
  Полгода водку пил. <br>
