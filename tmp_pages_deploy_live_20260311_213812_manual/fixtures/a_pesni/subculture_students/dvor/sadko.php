  <strong>ВАРИАНТ<br>
  <br>
  Садко - богатый гость </strong><br>
  <br>
  Три дня не унимается, <br>
  Бушует океан.<br>
  Как хуй в пизде, болтается<br>
  Кораблик по волнам. <br>
  <br>
  В каюте класса первого<br>
  Садко – богатый гость, - <br>
  Ругается по матушке,<br>
  Вбивает хуем гвоздь.<br>
  <br>
  «О, други мои верные,<br>
  Послушайте меня!<br>
  Из жертвы, в море брошенной,<br>
  Не вышло ни хуя! <br>
  <br>
  В (у)жопе человеческой<br>
  Сидит сам Царь морской.<br>
  Давайте же, товарищи,<br>
  Пожертвуем собой!» <br>
  <br>
  И жребий брошен был,<br>
  Вздохнули все легко.<br>
  Пал жребий на зачинщика,<br>
  На самого Садко. <br>
  <br>
  В тоске Садко сбирается,<br>
  Берёт свой чемодан,<br>
  Берёт гондонов дюжину<br>
  И томик Мопассан. <br>
  <br>
  Берёт бутыль огромную -<br>
  «Тройной» одеколон.<br>
  И для Царя подводного -<br>
  Пердячий патефон. <br>
  <br>
  Вот крестится, бросается<br>
  В пучину волн морских.<br>
  Взглянули из жопы яйца,<br>
  И окиян затих. <br>
  <br>
  Вот наш Садко на дне морском<br>
  Как взъёбанный стоит,<br>
  А перед ним является<br>
  Сам Чудо-Юдо Кит. <br>
  <br>
  Плывёт, мудями шевелит.<br>
  Наёбся, видно, всласть.<br>
  «Как во дворец попасть!» -<br>
  Кричит ему Садко. <br>
  <br>
  Кит пёрнул оглушительно:<br>
  «Доставим как-нибудь!»<br>
  Посрал, поссал и медленно<br>
  Он тронул в дальний путь. <br>
  <br>
  Кит едет очень медленно,<br>
  А ехать далеко.<br>
  На все четыре стороны<br>
  Любуется Садко. <br>
  <br>
  Под кустиком коралловым<br>
  Медузу ебёт рак.<br>
  Садко в недоумении - <br>
  Здесь воля иль бардак? <br>
  <br>
  Там кто-то задом пятится,<br>
  Блестят его глаза.<br>
  Свернулась каракатица,<br>
  Как старая пизда. <br>
  <br>
  А вот ещё картиночка,<br>
  Достойная пера:<br>
  Малюточку-сардиночку<br>
  Ебут два осетра.<br>
  (Очень хорошо, это очень сближает!) <br>
  <br>
  Кругом кровосмешение,<br>
  Здесь некого учить.<br>
  Садко глядел в смущении<br>
  И начал хуй дрочить. <br>
  <br>
  Пока к дворцу подъехали,<br>
  Он кончил три разА.<br>
  Кругом плотва со смехами<br>
  Всё пялила глаза. <br>
  <br>
  Глядит Садко – шикарный дом<br>
  Среди хрустальных вод.<br>
  А в том &lt;дому&gt; царит Содом -<br>
  Ебёт кого-то в рот. <br>
  <br>
  Решил Садко: не хочет Царь<br>
  Наследников плодить.<br>
  И говорит: «О государь,<br>
  Мне надобно платить! <br>
  <br>
  Тебе гондонов я принёс,<br>
  «Тройной» одеколон.<br>
  И для тебя специально вёз<br>
  Пердячий патефон. <br>
  <br>
  Вот, если хочешь, Мопассан, <br>
  Чтоб на ночь почитать».<br>
  Но Царь его прервал: «Пацан, <br>
  Дай бабу доебать!» <br>
  <br>
  Ебёт её и раз и два,<br>
  А буря всё сильней.<br>
  Садко подумал: «Ну дела!<br>
  Потопит он людей». <br>
  <br>
  Когда же кончил наконец,<br>
  То просветлел лицом<br>
  И начал мерить на конец<br>
  Гондоны и кольцо. <br>
  <br>
  «А ну, что просишь, говори!»<br>
  Садко ему в ответ:<br>
  «Корабль носишь – отпусти!»<br>
  Но царь ответил «нет». <br>
  <br>
  Тогда завёл он патефон,<br>
  И царь пустился в пляс.<br>
  И все придворные кругом<br>
  Задёргались тотчас. <br>
  <br>
  Танцуют час, танцуют два,<br>
  А буря всё сильней.<br>
  Царя усталость проебла, <br>
  И всех его друзей. <br>
  <br>
  Он хочет встать, он хочет сесть,<br>
  А ноги в пляс идут.<br>
  Он хочет срать, он хочет ссать,<br>
  А &lt;неразборчиво&gt;. <br>
  <br>
  Пришёл с &lt;неразборчиво&gt; трубой:<br>
  «Я вас освобожу!»<br>
  «А ну уёбывай домой,<br>
  Я больше не дрочу!» <br>
  <br>
  Тотчас и буря улеглась,<br>
  Хвалу принёс Творцу.<br>
  И нерв взволнованных рассказ<br>
  Уж подошёл к концу. <br>
