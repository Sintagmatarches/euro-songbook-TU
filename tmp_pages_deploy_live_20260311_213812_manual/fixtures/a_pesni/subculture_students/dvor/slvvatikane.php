  Этот случай был в городе Риме,<br>
  Там служил молодой кардинал.<br>
  Утром в храме махал он кадилом,<br>
  А по ночам на гитаре играл.<br>
  <br>
  В Ватикане прошел мелкий дождик,<br>
  Кардинал собрался по грибы,<br>
  Вот подходит он к римскому папе:<br>
  - Папа, папа, ты мне помоги.<br>
  <br>
  Тут папа быстро с лежанки свалился,<br>
  Он узорный надел свой спинжак,<br>
  Он чугунною мантией покрылся<br>
  И поскорее спустился в бельведер.<br>
  <br>
  И сказал кардиналу тот папа:<br>
  - Не ходи в Колизей ты гулять,<br>
  Я ведь твой незаконный папаша,<br>
  Пожалей хоть свою римскую мать.<br>
  <br>
  Но кардинал не послушался папи<br>
  И пошел в Колизей по грибы,<br>
  Там он встретил монашку младую,<br>
  И забилося сердце в груди.<br>
  <br>
  Кардинал был хорош сам собою,<br>
  И монашку сгубил кардинал,<br>
