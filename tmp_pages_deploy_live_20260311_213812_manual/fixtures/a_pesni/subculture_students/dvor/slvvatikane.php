  Об кадило гитарку разбил.<br>
  Рано утром ушел с Ватикану<br>
  И на фронт добровольцем пошел.<br>
  <br>
  Он за родину честно сражался,<br>
  Своей жизни совсем не щадил,<br>
  Сделал круглым меня сиротою,<br>
  Он папашей и дядей мне был.<br>
