<br>
  Старинный студенческий гимн, восходящий к эпохе Средневековья. Сочинен в XIV 
  или XIII веке предположительно студентами Гейдельбергского университета. Мелодию 
  написал в XV веке фламандский композитор Окенгейм. Впервые гимн опубликован 
  в 1776 году. Действующую по сей день стихотворную редакцию ему придал в 1781 году 
  немецкий поэт К. В. Киндлебен. Первый русский текст создал профессор Московского 
  университета математик Н. В. Бугаев.<br>
  <br>
  Хоровое исполнение гимна очень эффектно. Как показывает новейшая практика, очень 
  живописен при студенческих волнениях - ни с чем спутаешь. В частности, Gaudeamus 
  был использован во время волнений в Тюменском государственном университете (ТюмГУ) в 
  апреле 2005 года, когда российские власти предприняли попытку массовых слияний 
  и укрупнений вузов. ТюмГУ пытались ликвидировать, слив его с другим крупнейшим 
  вузом области - Тюменским государственным нефтегазовым университетом. 12 апреля 
  студенты ТюмГУ открыли пением Gaudeamus'а пикет против слияния, состоявшийся в Сквере Прощания, 
  расположенном между Главным корпусом ТюмГУ и корпусом историков. Протестная 
  кампания была выиграна - вузы в Тюмени не были слиты.<br>
