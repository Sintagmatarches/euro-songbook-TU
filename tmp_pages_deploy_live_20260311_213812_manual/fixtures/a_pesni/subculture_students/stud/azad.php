<br>
	"Гимн", сочиненный, вероятно, в 1948 г. В.С. Бахтиным и Д.М. Молдавским - соответственно, студентом и аспирантом Ленинградского государственного университета, в ходе их фольклорных экспедиций в Псковскую область к сказочнику Богатыреву. &quot;Сказки Богатырева&quot; планировалось выпустить отдельной книгой, она была одобрена издательством и научным руководителем профессором М.К. Азадовским. Из-за борьбы с космополитизмом книга не вышла, Азадовский в 1949 г. был уволен из университета. Тем не менее, Бахтин и Молдавский стали известными фольклористами. В основе гимна - песня <a href="http://a-pesni.org/ww2/oficial/3tankista.php">"Три танкиста"</a> (муз. братьев Покрассов, сл. Бориса Ласкина, 1939). Последняя строка (она выбивается из поэтического размера гимна) - перефразировка строки "Ну а девушки, а девушки потом" из песни <a href="http://a-pesni.org/ww2/oficial/pereletnyje.htm">"Перелетные птицы"</a> (муз. Василия Соловьева-Седого, слова Алексея Фатьянова, 1945). Обе песни - из кино.<br>
