		<br>
		Отправившись в Псковскую область во второй или третий раз все к тому же Богатыреву, мы с Молдавским по старому фольклорному способу, устно, в пути сочинили на мотив песни "На границе тучи ходят хмуро" свой гимн фольклористов (подобный гимн, как рассказывали старшие аспиранты, был в ходу и до войны). И всю дорогу, на машине и в телеге, в пешем строю и в купе вагона, весео распевали его. А чего ж не петь и не радоваться: аспирант и студент третьего курса вот-вот станут авторами отличной толстой книги, которая уже одобрена руководителем и издательством! И договор в кармане!..<br>
