1. Как получим диплом </strong><br>
Вариант ЛИАП<br>
<br>
Как получим диплом - а-бу-ди-бу-ду-да,<br>
Махнем в деревню.<br>
Соберем чуваков - а-бу-ди-бу-ду-да,<br>
И вспашем землю.<br>
Мы будем сеять рожь-овес - а-бу-ди-бу-ду-да,<br>
Ломая плуги.<br>
И прославим колхоз - а-бу-ди-бу-ду-да,<br>
По всей округе.<br>
<br>
Жил в деревне старик,<br>
Ел щи да кашу.<br>
Заманили его<br>
Мы в банду нашу.<br>
Стал дед отличным чуваком:<br>
Через соломку<br>
Лихо тянет коньяк<br>
И самогонку.<br>
<br>
Ты в навозе стоишь,<br>
Юбка с разрезом.<br>
Тихо тянешь быка<br>
За хуй облезлый.<br>
Ах ты, чува, моя чува,<br>
Тебя люблю я.<br>
За твои трудодни<br>
Тебя целую.<br>
