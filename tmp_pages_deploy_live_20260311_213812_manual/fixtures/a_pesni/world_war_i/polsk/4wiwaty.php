	<strong><span class="header">
Cztery wiwaty<br>
<em>(Mazurek. Na nutę: "Żyli ludzie przed Krakusem")</em><br></span>
Четыре тоста<br>
<em>(Мазурка. На ноты: "Жили люди до кракуса")</em><br></strong>
<br>
Słowa: W. P.<br>
<br>
Неj! Polacy! <br>
Yej rodacy! <br>
W tak szczęśliwej dobie, <br>
Stańmy w koło i wesoło <br>
Zaśpiewajmy sobie!<br>
<br>
Przeszły chwile, <br>
W których tyle <br>
Cierpieliśmy razem;<br>
Dziś uderzmy <br>
I odbierzmy<br>
Ojczyzęś żelazem.<br>
<br>
Niech wróg hardy, <br>
Godne wzgardy <br>
Ciągnie tłuszcze mnogie; <br>
Pójdziem śmiało, <br>
Walczyć z chwałą, <br>
Za swobody drogie.<br>
<br>
Szablą błyśniem, <br>
Kulą świśniem, <br>
Działami zaryczym; <br>
Pierwej mieczem, <br>
W pień wysieczem, <br>
Potem wrogów zliczym.<br>
<br>
Wszak wśród świata, <br>
Nasz Sarmata, <br>
W męstwie nie zadrzymał; <br>
Nikt mu w bitwach, <br>
Ni w gonitwach <br>
Placu nie dotrzymał.<br>
<br>
Му przykładów, <br>
Z naszych dziadów, <br>
Dosyć w dziejach mamy; <br>
Wieść nie brednia, <br>
Со do Wiednia <br>
I Kijowskiej bramy.<br>
<br>
Ojców godne, <br>
Nieodrodne, <br>
Wolnych przodków plemię, <br>
Głupiej dumie, <br>
Bić nie umie, <br>
Czołem własną ziemię.<br>
<br>
Gdy stal szczęknie, <br>
Wróg zajknie, <br>
Warkną śmierci groty;<br>
W duchu bratnim, <br>
Trzy łby płatniem. <br>
Niech zginą despoty.<br>
<br>
Dziś w pokoju, <br>
Му do boju, <br>
Polecim na blizny; <br>
Kielich w górę, <br>
Odgłos w chmurę, <br>
Za wolność Ojczyzny.<br>
<br>
Teraz w kolej, <br>
Wina dolej, <br>
Zagrzmi pieśń wesoła;<br>
Niechaj żyje, <br>
Z nami pije<br>
Podchorążych szkoła!<br>
<br>
  Toast trzeci, <br>
  Za was leci <br>
  Cni akademicy; <br>
  Coście żwawo, <br>
  Z chlubą, sławą <br>
  Bronili stolicy.<br>
  <br>
  Kielich czwarty, <br>
  Któż uparty, <br>
  Nie wychyli duszkiem; <br>
  Wam cześć, chwała, <br>
  Wdzięczność cała, <br>
  Dwernicki z Kościuszkiem!<br>
  <br>
  Teraz basta! <br>
  Wnuki Piasta, <br>
  Skończone wiwaty; <br>
  Pić nie wiele, <br>
  Walczyć śmiele, <br>
  To cnotą Sarmaty!<br>
