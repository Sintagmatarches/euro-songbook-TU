<br>
Bywaj dziewczę zdrowe, Ojczyzna mnie woła! <br>
Idę za kraj walczyć wśród rodaków koła: <br>
I choć przyjdzie ścigać jak najdalej wroga, <br>
Nigdy nie zapomnę, jak mi jesteś droga.<br>
<br>
Pocóż ta łza w oku, pocóż serca bicie? <br>
Tobim winien miłość, а Ojczyźnie życie! <br>
Pamiętaj żeś Polka, że to za kraj walka, <br>
Niepodległość Polski, to twoja rywalka.<br>
<br>
Polka mnię zrodziła, z jej piersi wyssałem: <br>
Вуć Ojczyźnie wiernym, а kochance stałym .<br>
I choć przyjdzie zginąć w ojczystej potrzebie, <br>
Nie rozpaczaj dziewczę, zobaczym się w niebie.<br>
<br>
«Czuję to, bom Polka: Ojczyzna w potrzebie,<br>
Nie pamiętaj о mnie, nie oszczędzaj siebie: <br>
Kto nie zna jej cierpień i głos jej przytłumi,<br>
Nie godzien miłości i kochać nie umie.<br>
<br>
Ty godnie odpowiesz, о to się nie troszczę,<br>
Że jesteś mężczyzną, tego ci zazdroszczę, <br>
Ty znasz moje serce, ty znasz moją duszę, <br>
Żem nie zdolna walczyć, tyleż cierpieć muszę.<br>
<br>
Nie mów o rozpaczy, bo to słabość duszy,<br>
Bo mię tylko jarzmo mego kraju wzruszy. <br>
Gdziebądź się spotkamy, spotkamy się przecie <br>
Zawsze Polakami, chociaż w innym świecie».<br>
