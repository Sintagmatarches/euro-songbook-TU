   Н. Агнивцев<br>
   <br>
  Из-под кочек, из-под пней<br>
Лезет враг оравой.<br>
Гей, казаки! на коней<br>
И айда за славой.<br>
Мать, не хмурь седую бровь,<br>
Провожая сына!<br>
Ты не плачь, моя любовь,<br>
Зоренька-девчина!<br>
Ты судьбине не перечь,<br>
Не кручинься слезно -<br>
Всем придется в землю лечь<br>
Рано или поздно.<br>
Помни: срока своего<br>
Смерть не проворонит,<br>
А кому не срок — того<br>
И в бою не тронет.<br>
На врагов, чертям на зло,<br>
Налетим мы бурей!<br>
Это наше ремесло -<br>
Целоваться с пулей!<br>
Эх, зудит моя рука,<br>
Будет с немцем рубка!<br>
Помолись за казака,<br>
Белая голубка!<br>
Тает, тает сизый дым,<br>
Ты прощай, станица!<br>
Мы тебя не постыдим -<br>
Будем лихо биться.<br>
Отшвырнем с родной земли<br>
Немцев в их берлогу,<br>
Хоть бы даже к ним пришли<br>
Черти на подмогу.<br>
Пусть придут! Среди гостей<br>
Будет больше крику,<br>
Потому что... и чертей<br>
Мы возьмем на пику.<br>
Эх, зудит моя рука -<br>
Будет немцам рубка!<br>
Помолись за казака,<br>
Белая голубка!<br>
