	<br>Дело славное на Сане<br>
	  Было, братцы, на реке.<br>
	  Наши храбрые саперы<br>
	  Отправлялись налегке,<br>
	<br>Темной ноченькой грузили<br>
	  Самодельные ладьи<br>
	  И тихонько подвозили<br>
	  По окольному пути.<br>
	<br>Бревна, доски подвозили<br>
	  Мы под пулями врага,<br>
	  Рамы, бочки и понтоны,<br>
	  Троссы, снасти, якоря.<br>
	<br>Под шрапнелями спустили<br>
	  Мы понтоны на реку<br>
	  И канатик укрепили<br>
	  На австрийском берегу.<br>
	<br>Переправили пехоту,<br>
	  Пулеметы. Австрияк<br>
	  Видит тут, что дело плохо, -<br>
	  Он пустился утекать.<br>
	<br>Но саперы не дремали:<br>
	  Мост с полночи навели -<br>
	  Утром Сан перешагнули<br>
	  Молодецкие полки.<br>
	<br>Восемь дней Австриец бедный<br>
	  Без оглядки удирал,<br>
	  Побросав патроны, ранцы:<br>
	  «Дело скверное!» - шептал.<br>
	<br>Не совался бы, дружище,<br>
	  Ты с Россией воевать,<br>
	  Не пришлось бы, хоть и стыдно,<br>
	  Без оглядки удирать.<br>
