	<span class="header"><strong> ОКОПНАЯ БАРЫНЯ</strong></span><br>
	<br>
    Ай, люли, люли, люли.<br> 
Как мы песню завели.<br> 
<div style="margin-left:32px">Барыня, барыня, сударыня барыня*.</div> 
Пишет, братцы, мне жена:<br> 
«Скоро ль кончится война?<br> 
Надоела нам она.<br> 
Истомилась я одна.<br> 
Истомилась, исстрадалась.<br> 
С милым век, как не видалась.<br> 
Стала жизнь мне сущий ад.<br> 
Ничего нейдет на лад.<br> 
Без хозяине все врозь.<br> 
Все хозяйство расползлось.<br> 
Опустел сарай и хлев.<br> 
Хлеба только на обсев.<br> 
Ты, голубчик мой Семен,<br> 
Дождалися мы времен.<br> 
Нет царя уже в нас боле.<br> 
Весь народ на вольной воле.<br> 
Всех чертей прогнали взашей,<br> 
Кто кормился кровью нашей.<br> 
Вот какие в нас дела.<br> 
Революция была.<br> 
Пропиши скорей ответ.<br> 
Жив, родимый, ты аль нет?<br> 
Долго ль будешь воевать,<br> 
Долго ль нам тут горевать?<br> 
Шлю с детьми тебе поклон.<br> 
Дорогой мой муж Семен».<br> 
А я дело, братцы, сметил,<br> 
А я женушке ответил:<br> 
«Не горюй, не плачь, жена.<br> 
Скоро кончится война.<br> 
Надоела нам она.<br> 
Бойня начата не нами,<br> 
А царями, да панами.<br> 
Так за их, за денежки,<br> 
Неча лесть нам на ножи.<br> 
Так ты всем и расскажи,<br> 
Что у немцев, даст бог, тоже<br> 
Выйдет дело с нашим схоже.<br> 
Ходят к нам их ходоки.<br> 
Люд фабричный, батраки.<br> 
Мы к ним с братскими словами,<br> 
Дескать, дело все за вами.<br> 
Сообща, ребятки, дружно<br> 
Богачей свергать нам нужно.<br> 
Отвечают немцы «гут».<br> 
И уж, видно, не солгут.<br> 
Как вернемся мы домой,<br> 
Что-то будет, боже мой!<br> 
Что случится, то увидим,<br> 
А себя уж не обидим.<br> 
Постоим з анашу долю,<br> 
За землицу, да за волю.<br> 
Если, жинка, доживу,<br> 
Буду дома к покрову.<br> 
Остаюсь жив и здоров<br> 
Рядовой Семен Петров».<br> 
Ох, барыня, барыня, барыня сударыня.<br> 
	<br> 
    <em>* Припев: «барыня, барыня...» повторяется после каждых двух строк.</em><br> 
    <br> 
