	<br>Хороша моя земелька,<br>
	  Только славушка худа,<br>
	  И уж так она худа:<br>
	  Не годится никуда.<br>
	<br>Хороши мои солдаты, -<br>
	  Лучше в мире не найдешь,<br>
	  Только очень вороваты<br>
	  И ретивы на грабеж.<br>
	<br>И устойчивы, и прытки,<br>
	  Но, куда ни попадут,<br>
	  Там проси-прощай пожитки:<br>
	  Все растащут, раскрадут.<br>
	<br>Генералы, офицеры,<br>
	  Да хранит их мне Аллах,<br>
	  Войску подали примеры<br>
	  В этих доблестных делах.<br>
	<br>Мой сынок, как и папаша,<br>
	  Полководец хоть куда, -<br>
	  Хороша земелька наша,<br>
	  Только славушка худа.<br>
	<br>Не боится Фридрих пули<br>
	  И в сраженьи очень смел,<br>
	  Но тарелки и кастрюли<br>
	  Воровать — собаку съел.<br>
	<br>Знайте, вороги, - не слабы<br>
	  Удалые пруссаки:<br>
	  Берегитесь, девки, бабы<br>
	  Да седые старики.<br>
	<br>Хороша земля родная,<br>
	  Только есть одна беда,<br>
	  Всюду слава ей шальная, -<br>
	  Вот что плохо, господа.<br>
