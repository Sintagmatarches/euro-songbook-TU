	<em>52. Текст записан т. Гуревичем А.В. со слов т. Госькова Семена в с. Верхний Ангарск, Байкальского района, БМАССР, 1927 г.<br>
<br>53-58. Тексты записаны в с. Качуг и с . Косая Степь, Качугского района, Иркутской области, 1914 г. Частушки публикуются по фольклорному архиву фольклорной секции Иркутского государственного научного музея.<br>
