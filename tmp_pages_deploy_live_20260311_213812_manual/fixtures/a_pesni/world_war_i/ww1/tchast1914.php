	  Кофию нагрохаю,<br>
	  Мово милого забреют,<br>
	  Три года проохаю.<br>
	<br>57<br>
	  С горки камешек катился,<br>
	  Прямо в реченьку упал,<br>
	  Несчастный мой миленок<br>
	  Во солдатики попал.<br>
	<br>58<br>
	  Скоро ягодки поспеют,<br>
	  Мы по ягодки пойдем.<br>
	  Скоро рекрутов забреют,<br>
	  Провожать мы их пойдем.<br>
	<br>59<br>
	  Ой, не плачь-ка, мать, отец,<br>
	  Нас погонят, как овец.<br>
	  Если ноги не несут,<br>
	  То на конях повезут,<br>
	  На конях на гнедых<br>
	  Ребятишек молодых.<br>
	<br>60<br>
	  Тебя, миленький, забреют,<br>
	  Я деваюся куда?<br>
	  На пруду широка прорубь,<br>
	  Суну голову туда.<br>
	<br>61<br>
	  Ах, ты, милая, не пой:<br>
	  Я теперечки не твой,<br>
	  Я теперь казенный сын,<br>
	  Под надзором под большим.<br>
	<br>62<br>
	  Много звездочек на небе<br>
	  На восточной стороне.<br>
	  Мой-от миленький страдает<br>
	  На великой на войне.<br>
<br>63<br>
	  Некрута, некрутики<br>
	  Наломали да прутики,<br>
	  На дорожку да ставили,<br>
	  Поминочек оставили,<br>
	  Поминочек оставили<br>
	  И горевать заставили.<br>
