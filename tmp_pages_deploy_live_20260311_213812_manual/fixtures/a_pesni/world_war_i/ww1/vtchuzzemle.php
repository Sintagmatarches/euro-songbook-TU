	  Я в Германии, в плену,<br>
	  Каждый день и голодаю,<br>
	  Наверно, с голоду помру!<br>
	  Не будут знать жена и дети,<br>
	  Где покончу жизнь свою!<br>
	  Я сплю — конюшна грязна,<br>
	  А кругом-то все навоз.<br>
	  Рано будят на работу,<br>
	  Слышу тысячи угроз,<br>
	  А я тощий едва тащуся,<br>
	  И желудок мой пустой.<br>
	  А конвой по-зверски смотрит:<br>
	  «Ты работай, Русь, не стой!»<br>
	  Знать, судьба моя такая,<br>
	  Здесь в Германии, в плену,<br>
	  Знать-то, мира не дождуся -<br>
	  В руках злодея я помру.<br>
	  Скрою глазки, улыбнуся,<br>
	  Что покончил жизнь свою...<br>
