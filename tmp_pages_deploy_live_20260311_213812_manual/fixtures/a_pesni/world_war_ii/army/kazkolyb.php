	<h1>КАЗАЧЬЯ 
  КОЛЫБЕЛЬНАЯ ПЕСНЯ</h1>
  Слова Михаила Лермонтова<br>
  <br>
  Спи, младенец мой прекрасный, <br>
  Баюшки-баю. <br>
  Тихо смотрит месяц ясный <br>
  В колыбель твою. <br>
  Стану сказывать я сказки, <br>
  Песенку спою; <br>
  Ты ж дремли, закрывши глазки, <br>
  Баюшки-баю. <br>
  <br>
  По камням струится Терек, <br>
  Плещет мутный вал; <br>
  Злой чечен ползет на берег, <br>
  Точит свой кинжал; <br>
  Но отец твой старый воин, <br>
  Закален в бою; <br>
  Спи, малютка, будь спокоен, <br>
  Баюшки-баю.<br>
  <br>
  Сам узнаешь, будет время, <br>
  Бранное житье; <br>
  Смело вденешь ногу в стремя <br>
  И возьмешь ружье. <br>
  Я седельце боевое <br>
  Шелком разошью... <br>
  Спи, дитя мое родное, <br>
  Баюшки-баю. <br>
  <br>
  Богатырь ты будешь с виду <br>
  И казак душой. <br>
  Провожать тебя я выйду — <br>
  Ты махнешь рукой... <br>
  Сколько горьких слез украдкой <br>
  Я в ту ночь пролью!.. <br>
  Спи, мой ангел, тихо, сладко, <br>
  Баюшки-баю. <br>
  <br>
  Стану я тоской томиться, <br>
  Безутешно ждать; <br>
  Стану целый день молиться, <br>
  По ночам гадать; <br>
  Стану думать, что скучаешь <br>
  Ты в чужом краю... <br>
  Спи ж, пока забот не знаешь, <br>
  Баюшки-баю. <br>
  <br>
  Дам тебе я на дорогу <br>
  Образок святой: <br>
  Ты его, моляся богу, <br>
  Ставь перед собой; <br>
  Да готовясь в бой опасный, <br>
  Помни мать свою... <br>
  Спи, младенец мой прекрасный, <br>
  Баюшки-баю. <br>
  <br>
  1837-1838<br>
  <br>
  <span class='i'>"Отечественные записки", 1842 г., № 5<br>
  <br>
  Лермонтов М.Ю. Соч. в 2 т. Т. 1 / Сост. и комм. И.С. Чистовой. 
  М.: Правда, 1988.<br>
  </span> <br>
  <br>
  По преданию, написано под впечатлением «баюкашной» песни казачки, которую Лермонтов 
  слышал в станице Червленой на Тереке (по другой версии – в станице Старомышастовской 
  на Кубани). Первый мотив написал сам поэт, современники слышали ее в авторском 
  исполнении. В дальнейшем музыку писали более 50 композиторов: А.Е. Варламов 
  (1842), Полина Виардо (1865), Э.Ф. Направник (1875), А.Т. Гречанинов (1894), 
  Н.М. Ладухин (1895), А.С. Танеев (1899), Н.Н. Черепнин (1900), В. Ребиков 
  и другие. Наиболее известный мотив принадлежит А. Варламову. Фольклоризированные 
  варианты входили в казачий и солдатский репертуар. Очень популярна была у терских 
  казаков. <br>
