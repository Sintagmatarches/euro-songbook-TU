	<h1>РЫДАЯ ОТ ОБИДЫ… 
</h1>
<br>
Мальчишкам я завидовала с детства,<br>
  Жалела, что девчонкой родилась.<br>
  С мальчишками, что жили по соседству<br>
  Я как мальчишка каждый день дралась.<br>
  Что больно, не показывала виду<br>
  И первой лезла в драку не боясь.<br>
  А дома я рыдала от обиды<br>
  Зачем на свет девчонкой родилась?<br>
<br>
Но вот мы незаметно старше стали,<br>
  Исполнилось нам по 17 лет,<br>
  И в летчики мы вместе поступали,<br>
  Мальчишек взяли, мне сказали «Нет».<br>
  Из кабинета выйдя с гордым видом,<br>
  Мальчишек я поздравила, смеясь.<br>
  А дома я рыдала от обиды<br>
  Зачем на свет девчонкой родилась?<br>
<br>
Но вот война внезапно наступила,<br>
  Нас по фронтам разбросила она,<br>
  Домой мои мальчишки не вернулись,<br>
  Домой вернулась только я одна.<br>
  И прислонившись к тоненькой березке,<br>
  Расплакалась я, слез своих стыдясь.<br>
  Простите вы, мальчишки, эти слезы,<br>
  Ведь я на свет девчонкой родилась…<br>
