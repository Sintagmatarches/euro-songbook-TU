	<span class="header"><strong>БАЛЛАДА О 
ВЕЧНОМ ОГНЕ </strong><br></span> 
<br>
Александр Галич<br>
<br>
<em>Посвящается Льву Копелеву <br>
<br>
...Мне рассказывали, что любимой мелодией лагерного начальства 
  в Освенциме, мелодией, под которую отправляли на смерть очередную партию заключенных, 
  была песенка <a href="../../popular-in/tumbalalaika.htm">«Тум-балалайка»</a>, 
  которую обычно исполнял оркестр заключенных. <br>
<br>
...<a href="../../ww2-polsk/montecassino.htm">«Червоны маки 
  на Монте-Кассино»</a> — песня польского Сопротивления.</em><br>
<br>
<br>
…«Неизвестный», увенчанный славою бранной! <br>
  Удалец-молодец или горе-провидец?! <br>
  И склоняют колени под гром барабанный <br>
  Перед этой загадкою Главы Правительств! <br>
  Над немыми могилами — воплем! — надгробья…<br>
  Но порою надгробья — не суть, а подобья, <br>
  Но порой вы не боль, а тщеславье храните, <br>
  Золоченые буквы на черном граните!.. <br>
<br>
Все ли про то спето? <br>
  Все ли навек — с болью? <br>
  Слышишь, труба в гетто <br>
  Мертвых зовет к бою! <br>
  Пой же, труба, пой же, <br>
  Пой о моей Польше, <br>
  Пой о моей маме –<br>
  Там, в выгребной яме!.. <br>
  Тум-бала, тум-бала, тум-балалайка, <br>
  Тум-бала, тум-бала, тум-балалайка, <br>
  Тум-балалайка, шпил балалайка, <br>
  Рвется и плачет сердце мое!<br>
<br>
А купцы приезжают в Познань, <br>
  Покупают меха и мыло... <br>
  Подождите, пока не поздно, <br>
  Не забудьте, как это было! <br>
  Как нас черным огнем косило <br>
  В той последней слепой атаке... <br>
  «Маки, маки на Монте-Кассино», <br>
  Как мы падали в эти маки. <br>
  А на ярмарке — все красиво, <br>
  И шуршат то рубли, то марки... <br>
  «Маки, маки на Монте-Кассино», <br>
  Ах, как вы почернели, маки! <br>
<br>
Но зовет труба в рукопашный, <br>
  И приказывает — воюйте! <br>
  Пой же, пой нам о самой страшной, <br>
  Самой твердой в мире валюте!.. <br>
  Тум-бала, тум-бала, тум-балалайка, <br>
  Тум-бала, тум-бала, тум-балалайка, <br>
  Тум-балалайка, шпил балалайка, <br>
  Рвется и плачет сердце мое! <br>
  Помнишь, как шел ошалелый паяц <br>
  Перед шеренгой на Аппельплац, <br>
  Тум-балалайка, шпил балалайка, <br>
  В газовой камере — мертвые в пляс... <br>
<br>
А вот еще: <br>
  В мазурочке <br>
  То шагом, то ползком <br>
  Отправились два «урочки» <br>
  В поход за «языком»!<br>
  В мазурочке, в мазурочке, <br>
  Нафабрены усы, <br>
  Затикали в подсумочке <br>
  Трофейные часы! <br>
  Мы пьем, гуляем в Познани <br>
  Три ночи и три дня... <br>
  Ушел он неопознанный, <br>
  Засек патруль меня! <br>
  Ой, зори бирюзовые, <br>
  Закаты — анилин! <br>
  Пошли мои кирзовые<br>
  На город на Берлин! <br>
  Грома гремят басовые <br>
  На линии огня, <br>
  Идут мои кирзовые, <br>
  Да только без меня!.. <br>
  Там, у речной излучины, <br>
  Зеленая кровать, <br>
  Где спит солдат обученный, <br>
  Обстрелянный, обученный <br>
  Стрелять и убивать! <br>
  Среди пути прохожего — <br>
  Последний мой постой, <br>
  Лишь нету, как положено, <br>
  Дощечки со звездой.<br>
<br>
Ты не печалься, мама родная,<br>
  Ты спи спокойно почивай <br>
  Прости-прощай, разведка ротная, <br>
  Товарищ Сталин прщевай! <br>
  Ты не кручинься, мама родная, <br>
  Как говорят, судьба слепа, <br>
  И может статься, что народная <br>
  Не зарастет ко мне тропа... <br>
  А еще: <br>
  Где бродили по зоне каэры, <br>
  Где под снегом искали гнилые коренья, <br>
  Перед этой землей — никакие Премьеры, <br>
  Подтянувши штаны, не преклонят колени! <br>
  Над сибирской тайгою, над Камой, над Обью <br>
  Ни венков, ни знамен не положат к надгробью! <br>
  Лишь, как вечный огонь, как нетленная слава <br>
  Штабеля! Штабеля! Штабеля лесосплава! <br>
<br>
Позже, друзья, позже <br>
  Кончим навек с болью <br>
  Пой же, труба, пой же! <br>
  Пой и зови к бою!<br>
<br>
Медною всей плотью <br>
  Пой про мою Потьму! <br>
  Пой о моем брате –<br>
  Там, в Ледяной Пади!..<br>
<br>
...Пой, труба, не чуди коленцами, <br>
  Пой, труба, чтобы сила крепла, <br>
  И чтоб встали мы, как в Освенциме —<br>
  Взявшись за руки среди пепла!<br>
<br>
Ах, как зовет эта горькая медь <br>
  Встать, чтобы драться, встать, чтобы сметь!<br>
  Тум-балалайка, шпил балалайка, <br>
  Песня, с которой шли мы на смерть! <br>
  Тум-бала, тум-бала, тум-балалайка, <br>
  Тум-бала, тум-бала, тум-балалайка, <br>
  Тум-балалайка, шпил балалайка, <br>
  Рвется и плачет сердце мое! <br>
<br>
31 декабря 1968, г. Дубна <br>
