Музыка Жоржа Ипсиланти
<br>
<br>
Ты далеко. В руках моих от тебя письмо, <br>
Как светлый луч из темных туч. Я забыла всё: <br>
И боль, и обиду разлуки лихой, <br>
И помню я счастье, что было с тобой. <br>
Вернись ко мне, вернись скорей, милый мой! <br>
<br>
<em>Припев: </em><br>
<br>
Но ты далеко, а сердце вечно с тобой. <br>
Ты далеко, услышь мой зов, милый мой. <br>
Ты далеко, а мне одной нелегко. <br>
Обнять так хочется тебя, но ты далеко. <br>
<br>
Твое письмо я перечла уж не в первый раз. <br>
Ночь напролет я не сомкнула усталых глаз. <br>
И всё мне казалось, что ты предо мной, <br>
Что будто бы голос я слышу родной. <br>
Вернись скорей, вернись ко мне, милый мой! <br>
<br>
