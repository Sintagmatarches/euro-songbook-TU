<span class='bold'>3. Цветочница Анюта</span><br>
<br>
Музыка М. Табачникова<br>
Слова Г. Строганова<br>
<br>
И зимой, и весной <br>
Аромат полевой <br>
И цветочная пыль в магазине. <br>
И влюбленный пилот <br>
От меня не уйдет <br>
Без фиалок в плетеной корзине. <br>
Пулеметчик, радист <br>
И безусый танкист - <br>
Все приходят ко мне за цветами, <br>
Потому что цветы, <br>
И любовь, и мечты - <br>
Это счастье, добытое нами. <br>
<br>
Зайдите на цветы взглянуть! <br>
Всего одна минута. <br>
Приколет розу вам на грудь <br>
Цветочница Анюта. <br>
Там, где цветы, всегда любовь - <br>
И в этом нет сомнений! <br>
Цветы любви нежнее слов<br>
И лучше объяснений! <br>
<br>
И однажды весной <br>
Лейтенант молодой <br>
Целый час простоял в магазине, <br>
Взором, полным огня, <br>
Поглядел на меня <br>
И унес мое счастье в корзине. <br>
И с тех пор я грущу, <br>
Всюду друга ищу, <br>
Осыпаю военных цветами, <br>
Потому что цветы, <br>
И любовь, и мечты - <br>
Это счастье, добытое нами. <br>
<br>
Придет, придет когда-нибудь<br>
Счастливая минута.<br>
И упадет к тебе на грудь<br>
Цветочница Анюта.<br>
Там, где цветы, всегда любовь, -<br>
И в этом нет сомнений.<br>
Цветы любви нежнее слов<br>
И лучше объяснений!<br>
