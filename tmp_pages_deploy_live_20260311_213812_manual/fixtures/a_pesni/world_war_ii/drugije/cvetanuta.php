И зимой и весной аромат полевой<br>
И цветочная пыль в магазине -<br>
Ни моряк, ни пилот от меня не уйдет<br>
Без фиалок в цветочной корзине.<br>
И поэт, и артист, и отважный танкист -<br>
Все заходят ко мне за цветами,<br>
Потому что цветы, и любовь, и мечты -<br>
Это счастье, взращенное нами.<br>
<br>
<span class='i'>Припев:</span><br>
<br>
Зайдите на цветы взглянуть - <br>
Всего одна минута, - <br>
Приколет розу вам на грудь <br>
Цветочница Анюта. <br>
Там, где цветы, всегда любовь, <br>
И в этом нет сомненья. <br>
Цветы бывают лучше слов <br>
И лучше объясненья. <br>
<br>
Но однажды весной лейтенант молодой <br>
Взял корзину цветов в магазине. <br>
Взором, полным огня, он взглянул на меня <br>
И унес мое сердце в корзине. <br>
И теперь я грущу и забыть не могу <br>
Яркий блеск золотистого канта. <br>
Все смотрю я на дверь: из военных теперь <br>
Ожидаю того лейтенанта. <br>
<br>
Припев. <br>
