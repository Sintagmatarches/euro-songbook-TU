<br>
Музыка М. Табачникова<br>
Слова Г. Строганова<br>
<br>
И зимой и весной - <br>
Аромат полевой<br>
И цветочная пыль в магазине.<br>
Ни танкист, ни пилот<br>
От меня не уйдет<br>
Без фиалок в цветочной корзине.<br>
Седоусый чекист<br>
и безусый танкист:<br>
Все приходят ко мне за цветами.<br>
Потому что цветы -<br>
И любовь, и мечты, - <br>
это чувства, рожденные нами.<br>
<br>
<span class='i'>Припев:</span><br>
<br>
Зайдите на цветы взглянуть,<br>
Всего одна минута!<br>
Приколет розу вам на грудь<br>
Цветочница Анюта!<br>
Там, где цветы, - всегда любовь,<br>
И в этом нет сомненья.<br>
Цветы бывают ярче слов<br>
и лучше объясненья.<br>
<br>
И однажды весной<br>
Лейтенант молодой<br>
Целый день простоял в магазине.<br>
А когда уходил,<br>
Он фиалки купил<br>
И унес мое сердце в корзине...<br>
Без любимого я и сама не своя,<br>
Как мучительно время проходит!<br>
Я не знаю причин,<br>
Только к нам в магазин<br>
Молодой лейтенант не заходит...<br>
<br>
Но однажды в магазин<br>
В окружении корзин<br>
Заглянул почтальон на минуту.<br>
Он принес письмецо,<br>
Адресовано оно<br>
Адресовано лично Анюте.<br>
Он пишет: &quot;Кончится война,<br>
И я вернусь, Анюта!&quot;<br>
Приди-приди-приди, пора,<br>
желанная минута!<br>
Приди-приди-приди, пора,<br>
желанная минута!<br>
Там встретит с радостью тебя<br>
Влюбленная Анюта. <br>
