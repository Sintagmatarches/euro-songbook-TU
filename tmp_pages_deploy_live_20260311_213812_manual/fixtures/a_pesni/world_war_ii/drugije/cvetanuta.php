<h1>ЦВЕТОЧНИЦА АНЮТА</h1>

И зимой и весной<br>
Аромат полевой<br>
И цветочная пыль в магазине.<br>
Ни кузнец, ни пилот<br>
От меня не уйдет<br>
Без фиалок в плетеной корзине.<br>
Сероглазый танкист,<br> 
И пилот, и артист -
<br>
Все приходят ко мне за цветами,<br>
Потому что цветы -<br>
И любовь и мечты - <br>
Это счастье, взращенное нами.<br>
<br>
<span class='i'>Припев:</span><br>
<br>
Зайдите на цветы взглянуть -<br>
Всего одна минута.<br>
Приколет розу Вам на грудь<br>
Цветочница Анюта.<br>
<br>
Но однажды весной<br>
Лейтенант молодой<br>
Целый день простоял в магазине -<br> 
Он фиалки купил,
<br> 
Но когда уходил,
<br>
Он унес мое сердце в корзине.<br>
Без любимого я и сама не своя,<br>
Так томительно время проходит.<br>
Я не знаю причин,<br>
Только к нам в магазин<br>
Молодой лейтенант не заходит.<br>
