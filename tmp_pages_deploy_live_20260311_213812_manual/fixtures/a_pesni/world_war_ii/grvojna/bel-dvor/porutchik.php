		   <span class="header"><strong>ЛЕГЕНДА: РОМАНС «ПОРУЧИК ГОЛИЦЫН» ДОЛГОЕ ВРЕМЯ СЧИТАЛСЯ НАРОДНЫМ</strong><br></span>
	   <br>
		<em><strong>Поручика Голицына расстреляли  в Киеве, а автор знаменитой песни покончил с собой в Риге</strong></em><br>
        <br>
        Легендарный романс «Поручик Голицын» долгое время считался народным. Затем кое-кто из исполнителей шансона стал присваивать его авторство себе. Однако подлинным автором песни является русский генерал украинского происхождения, поэт и писатель Георгий Гончаренко (псевдоним – Юрий Галич).<br>
        <br>
        <strong>Знакомство автора с прототипом</strong><br>
<br>
          Георгий Гончаренко был представителем полтавского дворянства. Он родился в военной семье 10 июня 1877 года и всю жизнь посвятил военному делу, конному спорту, а также поэзии, литературе и журналистике. Под псевдонимом Юрий Галич генерал написал 14 книг повестей, рассказов и стихов, опубликовал сотни статей.<br>
        <br>В годы Гражданской войны бывший блестящий гвардеец генерал-майор Гончаренко оказался на Украине и, конечно же, служил при гетмане Скоропадском – начальником наградного отдела. Собственно, именно здесь, в Киеве, он и познакомился с прототипом романса – петербуржским поручиком Константином Голициным.<br>
        <br>
        <img src="a-golicyn1.jpg" alt="Генерал Георгий Гончаренко (Юрий Галич)" width="280" height="399"><br>
  <em>А может, вернёмся? Писатель Юрий Галич (генерал Георгий Гончаренко) сбежал из Киева в Сибирь к Колчаку, а потом отправился в Прибалтику</em><br>
        <br>Дело было в январе 1919 года, когда на Украине правила Директория во главе с Петлюрой и Винниченко. Историческая встреча произошла в кутузке Осадного корпуса сечевиков где-то на улице Пушкинской. Гончаренко, снятый с поезда петлюровскими постами под Одессой и опознанный как гетманский генерал, парился на нарах уже несколько дней, когда к нему подселили двух новых соседей: бывшего главбуха киевского Нового банка Беленького и юного Голицына. Первого арестовали за то, что ссужал деньги Скоропадскому, второго – по недоразумению. Его перепутали с престарелым дядей поручика, князем Голицыным, возглавлявшим «Протофис» – организацию, сделавшую в своё время Скоропадского гетманом.<br>
        <br>Нельзя сказать, чтобы встреча была радостной, особенно, учитывая решётку на окнах, стражу и постоянную опасность быть расстрелянным. И тем не менее генерал в воспоминаниях признавал: «Я очутился в новом обществе, разделившем моё одиночество самым трогательным для меня образом. К бухгалтеру приходила жена, к молодому князю приходила невеста. Обе женщины являлись не только с ласками, не только со словами утешения и надежды, но каждый раз приносили узелки со съестными припасами домашнего изготовления».<br>
        <br>В одной камере генерал Гончаренко и будущий герой песни провели целую неделю. На восьмой день начальство решило перевести трёх арестантов в другое место. В качестве охраны к ним приставили старенького сторожа, позвякивающего ключами в одном кармане и пригубленной бутылкой горилки в другом. <br>
        <br>Логика у любителя спиртного сильно хромала. Чтобы узники не сбежали, сторож взял в руки их вещи, в которых, на его взгляд, находились ценности. Он почему-то решил, что конвоируемые не решатся бросить вещи ради побега. Когда странная процессия вышла на Крещатик, генерал присел, чтобы завязать шнурок, а банкир и поручик рванули вперёд. Сторож бросился за ними, но на полпути остановился, вспомнив, что за его спиной остался Гончаренко. Георгий Иванович тем временем быстрой походкой шёл в противоположную сторону. Сторож только и смог, что сокрушённо потрясти ключами в спины беглецов.<br>
        <br>Судя по всему, эта киевская встреча была первой и последней в судьбе Юрия Галича и князя Голицына.<br>
        <br>
        <strong>Красный хоровод</strong><br>
<br>
          И генерал, и поручик приняли самое активное участие в боях против большевиков. Георгий Иванович вскоре всё же добрался до Одессы, откуда, совершив трёхмесячное плавание вокруг всего Евроазиатского континента, приехал в Сибирь – к адмиралу Колчаку. Но там он не задержался и после окончания Гражданской войны каким-то загадочным образом оказался в Таллинне. Здесь генерал уже как Юрий Галич вернулся к писательскому ремеслу и журналистке, сотрудничал практически со всеми русскоязычными изданиями Прибалтики и издал полтора десятка книг, в том числе двухтомник воспоминаний «Красный хоровод».<br>
          <br>
          <img src="a-golicyn2.jpg" alt="Константин Голицын" width="210" height="299"><br>
  <em>В комнатах наших сидят комиссары. Константин Голицын, следственное фото 1930 года (предоставлено для публикации Государственным отраслевым архивом СБУ)</em><br>
        <br>
        Константин Голицын также пробрался на юг, но дальше не поехал, а поступил в белогвардейскую Добровольческую армию генерала Деникина. Здесь, уже в чине штабс-капитана, он командовал сводной ротой, состоящей из бывших стрелков полка Императорской фамилии. Какое-то время вместе с князем служил и ещё один любопытный офицер – Юрий Гладыревский – личный друг Михаила Булгакова, ставший прототипом Шервинского из «Белой гвардии».<br>
        <br>Тёплым августовским днем 1919 года рота князя Голицына на плечах красных одной из первых ворвалась в Киев. Но, как известно, белые были разгромлены, а Киев остался большевистским ещё на семьдесят лет.<br>
        <br>В следующий раз Голицын вернулся в Киев летом 1920 года, но уже не как победитель, а как жалкий и оборванный военнопленный, попавшийся красным под Одессой. В то время шла война с белополяками, РККА остро нуждалась в командных кадрах, и князя быстро переделали в военспеца, вновь отправив на фронт. Так что Гражданскую войну Голицын окончил уже в Красной армии. Он вернулся в Киев, женился, поступил на советскую службу и зажил мирной жизнью, скрывая своё прошлое.<br>
