        <br>
        <strong>Последнее обещание</strong><br>
<br>
          Следственное дело по обвинению в контрреволюционной деятельности Голицына, бывшего князя, бывшего поручика, бывшего деникинца, управляющего делами Киевглавпроекта, около шестидесяти лет хранилось под №1919 в архиве КГБ УССР.<br>
        <br>Как следует из документов, Голицына арестовали морозной январской ночью 1931 года. Дело, по которому проходил князь, называлось весьма безобидно: «Весна». Но это только на первый взгляд. Дело было инспирировано ГПУ для уничтожения в СССР бывших генералов и офицеров царской армии, независимо от их заслуг перед советской властью. Для того чтобы оказаться арестованным, хватало одной неудачно оброненной фразы. Бывших же белых хватали и без этого – сам факт их службы в период Гражданской войны по другую сторону баррикад был и уликой, и обвинением, и приговором. Заставляли признаться только в одном: причастности к контрреволюционной офицерской организации. И подавляющее большинство арестованных, как правило, под пытками подписывали всё, что им подсовывали следователи.<br>
        <br>Над арестованными в Киеве почти 600 бывшими генералами и офицерами «трудились» не только сотрудники ГПУ, но и курсанты местной школы милиции, отрабатывавшие на подследственных приёмы рукопашного боя. Как результат – более 95% «признаний», почти 160 вынесенных смертных приговоров. Попал в это число и князь Голицын.<br>
        <br>Постановление о расстреле Константина Голицына было вынесено 20 апреля 1931 года. Однако расстреляли его лишь одиннадцатью днями позже вместе с бывшим прапорщиком Левицким и подполковником Белолипским, который в 20-е годы переквалифицировался в актёра и играл первые роли на подмостках киевских театров. Какая судьба постигла супругу Константина Александровича – неизвестно. Офицеров, расстрелянных по делу «Весна», закапывали в братских могилах на Лукьяновском кладбище. Там их останки покоятся и до сих пор.<br>
        <br>Трагически сложилась судьба и Георгия Гончаренко. В эмиграции он оказался один. По некоторым данным, его жена и двое детей, оставшихся в Советской России, были репрессированы. А в 1940 году, после того как в Ригу вступила Красная армия, НКВД добралось и до генерала. Обстоятельства его гибели доподлинно неизвестны, тем не менее сохранилась очень красивая легенда.<br>
        <br>По преданию, генерал Гончаренко, известный своей журналистской деятельностью и непримиримой позицией по отношению к советской власти, был арестован уже в первые часы после вступления в Латвию Красной армии. При аресте у него нашли двенадцать из четырнадцати книг, не хватало только «Красного хоровода». В НКВД хорошо знали, с кем имеют дело, и в сопровождении охраны генерала послали домой – за двухтомником. Но там, дождавшись, когда охранники выйдут из комнаты в коридор, 63-летний генерал быстро смастерил петлю и набросил себе на шею.<br>
        <br>Ещё до прихода в Латвию Красной армии Юрий Галич обещал знакомым, что живым в руки большевиков не дастся. И старый императорский гвардеец выполнил последнее в своей жизни обещание.<br>

