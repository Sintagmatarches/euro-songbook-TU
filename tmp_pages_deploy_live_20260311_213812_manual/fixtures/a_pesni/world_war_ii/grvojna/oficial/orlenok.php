		<br>
		Пьеса, посвященная событиям Гражданской войны в черте оседлости,   написана на идиш и издана в Харькове в 1936 г. М. Даниэль - псевдоним Марка Мееровича (1900-1940),  еврейского драматурга, отца писателя-диссидента Юлия Даниэля. Песню &quot;Орленок, орленок, гордая птица&quot; сочиняет и поет главный герой, 15-летний сирота из еврейского местечка в Минской области, в тюрьме, куда польские легионеры посадили пленных красноармейцев. После освобождения из тюрьмы &quot;Орленок...&quot; становится гимном полка.<br>
		<br>
		Перевод первоначального текста идиш (подстрочник Ани Сорокиной, 2010 год): <br>
		<br>
		<em>Орленок, орленок, гордая птица,<br>
Расправь свои крылышки широко<br>
И скажи нашей невесте,<br>
И скажи нашей маме:<br>
&quot;Любимая, нас ожидает смерть&quot;.<br>
<br>
Орленок, орленок (друг молодой),<br>
Лети через границу,<br>
Сквозь тучу, сквозь бурю, сквозь снег.<br>
И встретишь там Ленина,<br>
Передай ему привет,<br>
большой привет партии.<br>
(в другом месте пьесы последняя строка - &quot;И скажи ему – мы за них!&quot;)</em><br>
		<br>
		Пьеса шла в еврейских театрах страны. В том же 1936 г. Б.Х. Черняк перевел ее на русский язык, в 1937 г. <a href="ziamka.php">этот перевод</a> был опубликован. Текст песни в переводе Черняка: <br>
		<br>
        <em>Орленок, орленок — могучая птица,<br>
Лети ты в далекий мой край,<br>
Там мама-старушка по сыну томится,<br>
Родимой привет передай!<br>
<br>
Орленок, орленок — могучая птица,<br>
К востоку стреми свой полет,<br>
Взлети над Москвою, над красной столицей,<br>
Где Ленин любимый живет!<br>
<br>
Орленок, орленок, ему расскажи ты<br>
Про наших врагов, про тюрьму;<br>
Скажи, что в плену мы, но мы не разбиты<br>
И нас не сломить никому.</em><br>
<br>
В финале пьесы второй куплет у Черняка звучит иначе:<br>
<br>
<em>Орленок, орленок, на родине дальней<br>
Наш Ленин любимый живет,<br>
К нему ты лети и ему расскажи ты,<br>
Что смело глядим мы вперед.<br>
</em><br> 
Пьесу на русском с музыкой Виктора Белого  поставил Театр им. МОСПС, в постановке звучал лишь первый куплет &quot;Орленка&quot; в переводе Черняка. Вскоре Яков Шведов написал новый текст песни, в котором уже сам герой стал именоваться Орленком, а не только птица, к которой он обращается с просьбой. Действие перенесено Шведовым  в &quot;бурятские сопки&quot;. Песня Белого-Шведова в июле 1937 г. вышла в Москве отдельным изданием, скан которого приведен выше. <br>
<br> 
Внучка Шведова журналистка Юлия Гончарова <a href="a-vzleti.php">в статье 2005 года</a> (к столетию поэта) утверждает со слов деда, что тот написал новый текст по просьбе Виктора Белого, а вдохновило его пушкинское стихотворение &quot;Узник&quot;, где герой обращается к орлу. По ее же словам,  у Шведова на груди была татуировка в виде орла.<br>

<br>
		В поздних советских песенниках  &quot;бурятские степи&quot; заменяли на географически нейтральные &quot;ковыльные степи&quot;, а &quot;солдат&quot; на &quot;врага&quot;. Песня входила в репертуар Большого детского хора под управлением Виктора Попова. В 1968 г. на &quot;Союзмультфильме&quot; по ней снят одноименный мультфильм, а еще раньше, в 1957 г., вышел художественный фильм &quot;Орленок&quot; о вымышленном пионере-герое Великой Отечественной войны. <br>
