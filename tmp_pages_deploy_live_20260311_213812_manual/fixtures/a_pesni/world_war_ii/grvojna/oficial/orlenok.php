		У &quot;Орленка&quot; 
        есть <a href="../bel/orlenok-bel.php">антибольшевистский казачий вариант</a>. Он бытует на территории бывшего Оренбургского казачьего войска по меньшей мере с 1970-х годов. Видимо, &quot;антибольшевистская&quot; версия сочинена на основе текста официального &quot;Орленка&quot; в среде оренбургских казаков, недовольных советской властью. Ее автором вполне мог быть уцелевший участник Гражданской войны со стороны белых или повстанцев. Другого разумного объяснения в голову не приходит. <br>
        <br>
        Еще один интересный момент. У песен <a href="zelezniak.php">&quot;Партизан 
        Железняк&quot;</a> (музыка Матвея Блантера, слова Михаила Голодного, 1935), 
        <a href="pokahovke.php">&quot;Песня о Каховке&quot;</a> (музыка Исаака Дунаевского, слова Михаила 
        Светлова, 1936) и &quot;Орленок&quot; (музыка 
        Виктора Белого, слова Якова Шведова, 1936) одинаковая мелодия, и даже 
        манера исполнения, несмотря на разных композиторов. Это еврейская фольклорная 
        мелодия. Возможно, композиторы заимствовали ее неосознанно. <br>
