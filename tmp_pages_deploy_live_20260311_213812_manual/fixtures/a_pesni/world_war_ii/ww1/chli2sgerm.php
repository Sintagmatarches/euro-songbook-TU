        Шли два героя с германского боя.<br>
        И шли два героя домой.<br>
        Они повстречались на финской границе.<br>
        И финн из них ранил одного. <br>
        <br>
        «Товарищ, товарищ, болят мои раны,<br>
        Болят мои раны тяжело.<br>
        Одна засыхает, другая нарывает,<br>
        А с третьей придется умереть. <br>
        <br>
        А дома детишки, жена молодая,<br>
        Они ожидают домой».<br>
        «И пусть ожидают, не важное дело,<br>
        Но им не дождаться тебя». <br>
        <br>
        Детишки подрастают, мамашу пытают:<br>
        «И где же наш папа родной?»<br>
        Она отвернется, слезами зальется:<br>
        «Погиб на германской войне». <br>
        <br>
        <em>Неизвестный источник</em><br>
        <br>
        <br>
        Существует множество трагикомических военных и блатных песенок на этот 
        же мотив со сходным сюжетом: героя, который возвращается с войны или бежит 
        из тюрьмы, смертельно ранят враги (поляки, финны, чекисты), и он произносит 
        своему товарищу монолог со словами &quot;Товарищ, товарищ, болят мои раны…&quot;. 
        Либо монолог произносится в лазарете медсестре. Песенки эти известны с 
        дореволюционных времен, и продолжали сочиняться в годы советской власти. 
        Самая известная из них - <a href="../dvor/sodesskogo.php">&quot;С Одесского кичмана&quot;</a> (там же см. весь список). 
        Основой сюжета могла послужить песня на стихотворение Михаила Михайлова 
        <a href="../popular20/grenadery.htm">&quot;Из Франции два гренадера&quot;</a> 
        (перевод из Генриха Гейне, опубликован в 1846 г.).<br>
        <br>
        В данной версии могут иметься в виду финальные бои Первой мировой войны, 
        закончившиеся Брестским миром, по которому Финляндия досталась Германии, 
        а через несколько месяцев обрела независимость. Образ &quot;злобного финна&quot; 
        часто встречается в фольклоре тех лет (мир Советской России с Финляндией 
        подписан 14 октября 1920 г.). Или это Вторая мировая 
        война, в которой финны воевали на стороне Германии. <br>
