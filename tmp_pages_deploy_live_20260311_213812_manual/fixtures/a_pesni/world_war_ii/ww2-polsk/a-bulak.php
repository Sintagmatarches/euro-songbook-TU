        В ночь с 7 на 8/IX 1939 г. командующий фронтом, генерал дивизии Руммель<sup>1</sup> 
        разрешил генералу запаса Станиславу Балаховичу формировать добровольческие 
        отряды при Командовании обороной Варшавы. В эти добровольческие подразделения, 
        состоящие из пехоты и кавалерии, нельзя было принимать военнослужащих, 
        которые подлежат мобилизации. Командование обороной Варшавы назначило 
        начальником штаба в группу генерала Балаховича дипломированного майора 
        запаса Щудловского. Штаб был организован в следующем составе: подполковник, 
        военный инвалид Николай Шустров-Станиславский - заместитель командующего 
        группой; офицер по специальным поручениям - Константы Гротковский (офицер 
        бывшей армии Балаховича); офицер по оперативной работе - ротмистр N.N.<sup>2</sup>; 
        поручик запаса Халик - офицер по связям и информации; поручик запаса Герман 
        - интендант; капитан медицинской службы д-р Пасиковский - глава санитарной 
        службы. Подполковник Станиславский и майор Щудловский придали деятельности 
        этого штаба такой тем и размах (штаб находился первоначально по улице 
        Маршалковской, дом 72), что [в их распоряжении] уже 10 IX 39 г. оказался 
        эскадрон численностью 100 сабель под командованием ротмистра народного 
        ополчения Велёпольского, который в тот же день двинулся по направлению 
        на Минск-Мазовецкий. Функцию командира взвода в этом эскадроне исполнял 
        вахмистр запаса Владислав Пулаский<sup>3</sup> - член боевой организации 
        Национально-радикального лагеря (НРЛ)<sup>4</sup> на территории столицы, 
        пользующийся дурной, хоть и шумной славой. Пуласкому очень скоро надоело 
        честное ремесло солдата, он отделился от эскадрона и вернулся в Варшаву. 
        Там, облачившись в мундир со знаками различия ротмистра легкой кавалерии 
        и подобрав себе достойную компанию, он «реквизировал» из кассы универмага 
        «Торговый дом братьев Яблковских» десяток с лишним тысяч злотых - якобы 
        на нужды Добровольческой группы генерала Балаховича. Генерал приказал 
        схватить «господина ротмистра» и поставить его перед военно-полевым судом 
        за мародерство. Пулаский сумел хорошо спрятаться и дал о себе знать лишь 
        в декабрьские дни 1939 г. во время бездарно срежиссированных в гестапо 
        антисемитских выходок варшавской черни и подонков. В следующий раз фамилия 
        Пулаского ходила по разным кафе зимой 1940 г., когда начали циркулировать 
        слухи и версии о намерениях немцев создать так называемый «польский легион» 
        на случай вооруженного немецко-русского столкновения. Сегодня о Пуласком 
        ничего не слышно. <br>
        <br>
        Ночью 10/IX-39 г. ротмистр Константы Гротковский получил приказ организовать 
        оборону Вилянова. На рассвете этот варшавский район был уже оцеплен командой 
        численностью в 30 штыков и 15 сабель. Пехотой командовал поручик запаса 
        Кмит, кавалерией - поручик запаса Вернер; общее руководство обороной осуществлял 
        ротмистр Гротковский. Желая укрепить свою количественно слабую команду, 
        а прежде всего - разгрузить ее, избавив от обеспечения связи между Виляновом 
        и районом Повислья (там стоял взвод регулярной пехоты с двумя тяжелыми 
        пулеметами), ротмистр Гротковский мобилизовал 20 молодых жителей Вилянова, 
        вооружил и это подразделение гражданской обороны, а затем отдал его под 
        командование хорунжего Лобачевского (казачьего офицера из бывшей армии 
        Балаховича), который, однако, в боевой акции оказался трусом и, подвергнувшись 
        немедленному наказанию Балаховича, был изгнан из рядов его группы. 11/IX 
        39 г. в Вилянов поступило пополнение численностью 80 штыков под командованием 
        поручика Адо, русского эмигранта и бывшего офицера армии Балаховича с 
        1920 года. Под началом поручика Адо состояло несколько ротных офицеров: 
        подпоручик запаса Николай Качановский, подпоручик Ладковский, а также 
        хорунжий Касторов, военный инвалид без руки, русский эмигрант. Хорунжий 
        Касторов (бывший казачий офицер) в ночь с 12 на 13 сентября продемонстрировал 
        высокие боевые качества, будучи командиром сторожевого поста (на дороге, 
        которая вела к Повсину [что в 4 км от Вилянова]), атакованного сильным 
        разведывательным отрядом неприятеля. Во главе десятка бойцов он перешел 
        в контратаку, заставил немцев отступить и захватил четырех пленных, а 
        также легкий пулемет. Утром 13/IX к Вилянову подтянулся во главе отряда 
        из 90 сабель командующий кавалерией в группе Балаховича ротмистр Эугениуш 
        Штейнике (бывший офицер армии Балаховича). Поручик Вернер на рассвете 
        14/IX получил приказ патрулировать дорогу на Натолин. Во время патрулирования 
        натолинского парка на людей поручика Вернера неожиданно напал сильный 
        отряд немецких мотоциклистов. Не обращая внимания на численный перевес 
        неприятеля, поручик Вернер бросился в кавалерийскую атаку на врага и, 
        пронзенный несколькими пулями, пал на поле боевой славы. Из отряда поручика 
        Вернера уцелели три всадника. Приказом генерала Балаховича поручик Вернер 
        был посмертно представлен к награждению крестом «Virtuti Militari». Вечером 
        14/IX кавалерия ротмистра Штейнике отошла к фольварку Черняков, причем 
        на место погибшего поручика Вернера был назначен хорунжий Касторов, за 
        мужество повышенный в звании до подпоручика. В ночь с 14 на 15/IX адъютант, 
        подпоручик Ладковский, доложил ротмистру Гротковскому, что поручик Адо 
        склоняет офицеров покинуть ряды добровольческой группы генерала Балаховича 
        и перейти под командование полковника Михаила Яковлева (бывшего атамана 
        сводной казачьей дивизии польской армии в 1920 г.), который по поручению 
        генерала Чумы<sup>5</sup> и при поддержке старшего офицера отдела II<sup>6</sup> 
        из Командования обороной столицы создает так называемую специальную группу. 
        Яковлев, бывший артиллерийский штабс-капитан царской армии, был известен 
        как отличный партизан и организатор, он оставил о себе хорошую память 
        в истории партизанских боев с большевиками по ходу кампании 20-го года, 
        и отсюда проистекала его популярность среди бывших русских офицеров. Как 
        очень [нрзб.] сотрудник отдела II в Вильно, он пользовался доверием у 
        военных властей. <br>
        <br>
        Ротмистр Гротковский немедленно сообщил майору Щудловскому о донесении 
        подпоручика Ладковского. Генерал Балахович не придал особого значения 
        рапорту своего начальника штаба и, желая лично ознакомиться с настроениями 
        среди офицерского корпуса, отправился ночью 15 сентября на виляновский 
        участок фронта. Прибыл он туда как раз в процессе напряженного боя между 
        пехотинцами поручика Адо и вражескими войсками, атакующими со стороны 
        Повсина. После двухчасового сражения Адо отступил по направлению на фольварк 
        Черняков, имея приказ прибыть к ротмистру Штейнике<sup>7</sup> и доложиться. 
        Поручик Адо вроде бы выполнил этот приказ, но уже 15/IX утром покинул 
        Черняков и направился на сборный пункт, указанный Яковлевым. Вместе с 
        поручиком Адо пошел и подпоручик запаса Качановский. <br>
        <br>
        Дезертирство поручика Адо принудило ротмистра Штейнике отступить вплоть 
        до самого конца Черняковской улицы и здесь ожидать дальнейших указаний. 
        <br>
        <br>
        Тем временем в Варшаве «полковник» Яковлев приступил к действиям, направленным 
        на лишение генерала Балаховича поста командующего добровольческой группой. 
        С этой целью Яковлев в сопровождении своего адъютанта поручика Вальцева 
        (русского эмигранта) пришел в штаб-квартиру генерала Балаховича (на тот 
        момент она располагалась по улице Литовской, 3) и, предъявив письменный 
        приказ начальника отдела II при Командовании обороной Варшавы (дипломированного 
        майора М.), который давал ему право арестовать генерала Балаховича, потребовал 
        от офицера, выполнявшего при нем функции ординарца, ротмистра запаса Гурского, 
        отдать все документы группы и ее печать. Во время бесплодного спора между 
        Яковлевым и ротмистром Гурским явился прибывший из-под Вилянова генерал 
        Балахович, который без единого слова предъявил Яковлеву письменный приказ 
        генерала Чумы, назначающий Балаховича командующим IV группой оперативной 
        обороны Варшавы. Яковлев, не солоно хлебавши, попрощался с генералом и 
        отправился в сторону Грохова, где его заместитель, «полковник» Иосиф Юрченко 
        (русский эмигрант, бывший офицер царской армии, человек способный, но 
        двуличный и к тому же бесхарактерный пьяница) занимался организацией зачатков 
        так называемой специальной группы.<br>
        <br>
        Перед этой группой ставилась задача по проведению диверсионных акций и 
        контрразведывательной деятельности. Сведения об акциях «спецгруппы» Яковлева 
        очень скудны. Сам Яковлев был в апреле 1940 г. арестован немцами и помещен 
        в Освенцим, где умер в июле 1941 года. <br>
        <br>
        Дезертирство поручика Адо взвалило на плечи ротмистра Штейнике весь груз 
        ответственности за оборону участка в районе улиц Черняковской, Хелмской 
        и Бельведерской. Желая заполнить разрыв, образовавшийся после отряда поручика 
        Адо, генерал Балахович приказал ротмистру Гротковскому формировать так 
        называемые резервные роты. В течение трех дней было укомплектовано девять 
        таких рот, которыми ввиду нехватки офицеров командовали курсанты военных 
        училищ и даже сержанты «Стшельца»<sup>8</sup>. Эта импровизированная пехота 
        не только вытеснила врага с подступов к своему району, но даже нанесла 
        ему существенный урон в живой силе и военном снаряжении. Наиболее эффективной 
        была вылазка в ночь с 18 на 19/IX 39 г., которой лично руководил инвалид 
        без ноги, подполковник Станиславский; она принесла трофеи в виде шести 
        тяжелых пулеметов и 23 больших ящиков с боеприпасами. За эту удачную операцию, 
        проведенную под его личным командованием, подполковник Станиславский был 
        произведен в полковники и награжден крестом «Virtuti Militari» V класса. 
        <br>
        <br>
        23/IX в состав линейных подразделений вошел вновь организованный эскадрон 
        кавалерии под командованием бывшего начальника отделения конной полиции 
        Шопы. Этот эскадрон вместе со всей пехотой IV оперативной группы обороны 
        Варшавы занял участок в районе Белян, который они защищали вплоть до 28/IX, 
        т.е. до момента капитуляции. <br>
        <br>
        26/IX 39 г. полковник Станиславский по приказу генерала Балаховича принял 
        командование IV оперативной группой и в этом качестве произвел ее ликвидацию, 
        оставаясь до самого конца на своем посту как образец долга и воинского 
        достоинства.<br>
        <br>
        31/III 1940 г. полковник Станиславский был арестован, посажен в тюрьму 
        на Повислье и 21/VI 40 г. расстрелян в Пальмире под Варшавой. <br>
        <br>
        Генерала Балаховича в мае 1940 г. убило гестапо в районе Саска Кемпа, 
        у дома 23 на Парижской ул. Он награжден крестом «Virtuti Militari».<br>
        <br>
        Ротмистр Штейнике был арестован в 1940 г. и умер в «Павяке». Награжден 
        Крестом храбрых.<br>
        <br>
        Майор Щудловский был произведен в подполковники и получил крест «Virtuti 
        Militari».<br>
        <br>
        Капитан медицинской службы д-р Пасиковский был повышен в звании до майора 
        и получил крест «Virtuti Militari».<br>
        <br>
        Поручик медицинской службы д-р Стефан Онышкевич, младший врач добровольческой 
        группы, получил повышение — звание капитана.<br>
