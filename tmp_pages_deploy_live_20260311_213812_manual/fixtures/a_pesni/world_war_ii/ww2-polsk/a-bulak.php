        <span class="text"> <span class="header"><strong>ГЕНЕРАЛ СТАНИСЛАВ БУЛАК-БАЛАХОВИЧ 
        в 1939 году</strong></span> <br>
        <strong><br>
        <em>Публикация Андрея Кручинина и Петра Мицнера </em></strong><em><br>
        <br>
        «Новая Польша», №7-8 (121) / 2010, стр. 67-74.</em><br>
        <br>
        <br>
        <strong><em>От публикатора </em></strong><br>
        <br>
        Автор публикуемого документа, майор Станислав Славинский, был профессиональным 
        военным. Во время II Мировой войны служил в Отделе безопасности и контрразведки 
        Главного командования Армии Крайовой. Руководил там «сектором 994», занимавшимся 
        сбором и анализом сведений о малых подпольных организациях. В начале сентября 
        1944 г. был арестован гестапо в Кракове и 9 сентября того же года расстрелян. 
        Несколько лет назад горстка документов, связанных с памятью о нем и сохранившихся 
        совершенно случайно, через мое посредство попала в собрания центра «Карта»* 
        в Варшаве. Среди них - и настоящий рапорт, касающийся генерала Булак-Балаховича; 
        вероятно, основанием для него в определенной степени послужило собственное 
        сообщение генерала, которого Славинский, как представляется, знал лично. 
        <br>
        <br>
        <em>*Центр «Карта» — независимая архивная и издательская организация, 
        сформировавшаяся в 1987-1993 гг. и занимающаяся новейшей историей Польши 
        и Восточной Европы в целом; сотрудничает, в частности, с российским «Мемориалом». 
        </em><br>
        <br>
        <br>
        <strong>Андрей Кручинин </strong><br>
        <br>
        <span class="header"><strong>БАЛАХОВИЧ И БАЛАХОВЦЫ: СТРАНИЦЫ ИСТОРИИ <br>
        Предисловие к документу</strong> <br>
        </span> <br>
		<br>
        <img src="a-bulak.jpg" alt="Генерал Станислав Булак-Балахович" width="344" height="496" hspace="5" align="left">Два 
        имени, три фамилии, четыре национальности, пять воюющих армий - таким 
        причудливым даже для Смутного времени 1917-1920 гг. перечнем описывается, 
        но не раскрывается в полной мере биография генерала Булак-Балаховича, 
        человека поистине легендарного, еще при жизни окруженного слухами, анекдотами, 
        преданиями, зачастую фантастическими. Будучи католиком, он носил двойное 
        имя Станислав-Мариан; в годы Первой Мировой войны активно экспериментировал 
        с собственною фамилией, выбирая, какая будет лучше звучать - «Балахович», 
        «Булак-Балахович», «Бэй-Булак (или Булак-Бэй) Балахович»; в разные периоды 
        своей жизни мог считаться русским, белорусом, подчеркивать свои польские 
        корни, иногда же его считали литовским татарином; с честью сражаясь в 
        1914-1917 гг. в русской армии, он после революционной катастрофы оказывается 
        на советской службе, осенью 1918-го переходит в русские контрреволюционные 
        войска и с этого момента неустанно и упорно борется против большевиков 
        - в Северо-Западной армии генерала Н.Н. Юденича, в «Особом отряде Белорусской 
        Народной Республики в Прибалтике», плечом к плечу с польскими войсками 
        в Полесьи и, наконец, поздней осенью 1920 г., уже после заключения прелиминарного 
        советско-польского договора в Риге, выступает в самостоятельный поход 
        в качестве командующего Русской народной добровольческой армией (затем 
        - главнокомандующего всеми вооруженными силами на территории Белоруссии) 
        и в союзе с Белорусским политическим комитетом. Зигзаги судьбы генерала 
        Балаховича часто побуждают говорить о нем как о беспринципном авантюристе, 
        хотя такой ярлык представляется нам несправедливым. Вряд ли справедливо 
        и преувеличение стихийного, иррационального начала в личности Балаховича 
        - такое, например, как у писателя Д.С. Мережковского, общавшегося с генералом 
        в Варшаве в 1920 г. и характеризовавшего его как «стихию без сознания», 
        которая сама по себе «ничего не сделает». Показателен и нарисованный Мережковским 
        портрет Балаховича - странный, расплывчатый, но тем не менее впечатляющий: 
        «Тут сила не в словах, а в самой личности, лице Балаховича этом смугло-румяном, 
        обветренном, худом, костлявом, крепком, как бы железном, грубом и тонком, 
        детски-простом и страшно-сложном лице с болезненной складкой в губах, 
        выражением почти беспомощной слабости, и с глазами мутно-голубыми, жутко-пьяными, 
        - да, пьяными, но чем? Вином, кровью, славою, смертью? Нет. Так чем же? 
        Не знаю. Может быть, судьбою, - своею судьбою, малою или великою, но которую 
        надо ему совершить до конца. Где будет конец, погибнет ли «партизан» Балахович 
        в Бобруйске, Смоленске или дойдет до Москвы «главковерхом» (верховным 
        главнокомандующим. - <em>Авт.</em>), - я опять-таки не знаю. Знаю только, 
        что он уже идет - летит и долетит до конца, не остановится. Вот этим-то 
        концом он, может быть, и пьян»<sup>1</sup>. Пожалуй, более проницательной 
        оказалась супруга и единомышленница Мережковского, писательница и литературный 
        критик З.Н. Гиппиус, также глядевшая на генерала несколько свысока («Балахович 
        - интуит, дикарь и своевольник... Хитер, самоуверен и самолюбив. Совсем 
        не «умен», но в нем искорки какой-то угадки...»), но увидевшая и главное, 
        что, наверное, владело этим человеком: «Ненависть к большевикам - это 
        у него пламенная страсть»<sup>2</sup>. <br>
        <br>
        Не преувеличивает ли Гиппиус? Ведь с именем Булак-Балаховича в эпоху 1917-1920 
        гг. обычно связывают такие поистине «революционные» черты, как недисциплинированность, 
        произвол, потакание инстинктам толпы; ведь с этой точки зрения служба 
        его в Красной Армии в марте-октябре 1918-го и участие в карательных действиях 
        против крестьян Псковщины и Новгородчины зачастую считаются вполне закономерными; 
        ведь и Мережковский, размышляя о личности генерала, делал парадоксальный 
        вывод: «Этот антибольшевик - большевик наизнанку. В этом - страшная слабость 
        и страшная сила его, сила и слабость вместе»<sup>3</sup>, - а глава Польского 
        государства маршал Пилсудский считал, что Балахович «бьет большевиков 
        во многих случаях лучше, чем штабные генералы, поскольку сам большевик 
        - в конце концов, из них и происходит»<sup>4</sup>. Если верить Б.В. Савинкову, 
        сотрудничавшему и близко общавшемуся как с Пилсудским, так и с Балаховичем, 
        глава государства в личной беседе отзывался о генерале и вовсе с ноткой 
        презрения (правда, рассказывал это Савинков на советском суде): «Бандит, 
        но не только бандит, а человек, который сегодня русский, завтра поляк. 
        послсзавтра белорус, а еще через день — негр...»<sup>5</sup><br>
        <br>
        Портрет выходит довольно неприглядный; но подобная точка зрения на Станислава 
        Булак-Балаховича все-таки не отличается глубиной и объективностью. Человек 
        с беспокойным характером, вспыльчивый до необузданности, вместе с тем 
        легко попадавший под чужое влияние, - он, должно быть, и вправду имел 
        на совести немало грехов, но и список заслуг и отличий Балаховича тоже 
        не мал. Критически относясь ко многому в устройстве Российской Империи, 
        в 1914 г. Балахович добровольцем пошел в армию и стал лояльным и доблестным 
        воином. Будучи произведен в офицеры за боевые отличия (военного образования 
        будущий генерал не имел никакого), он храбро сражался сначала в рядах 
        2-го лейб-уланского Курляндского полка, а с конца 1915 г. - в конном «отряде 
        особой важности» Северного фронта, и командир отряда буквально пел ему 
        дифирамбы: «Показал себя с самой выдающейся стороны, выказав безусловно 
        огромную храбрость, решимость и редкую находчивость и предприимчивость. 
        В боевой партизанской работе это лихой незаменимый офицер, везде и всюду 
        идущий охотником и всегда впереди. (...) Показал себя талантливым офицером, 
        свободно управляющим сотней людей в любой обстановке и с редким хладнокровием, 
        глазомером и быстротой оценки обстановки. Постоянно ведет работу с минимальными 
        потерями»<sup>6</sup>.<br>
        <br>
        Храбрый солдат, дерзкий партизан, заботливый начальник - Булак-Балахович, 
        должно быть, вполне закономерно оказался наиболее авторитетным из офицеров 
        отряда в условиях деморализации, охватившей русскую армию после Февральского 
        переворота 1917 г. Балахович, похоже, искренне разделяет демократические, 
        умеренно-революционные взгляды, но вместе с тем столь же искренне считает 
        необходимым продолжение борьбы против немцев, к отказу от которой призывают 
        Ленин и его сторонники. В июле 1917-го Станислав Никодимович даже участвует 
        в карательной экспедиции против братавшихся с врагом пехотинцев. После 
        произошедшего в октябре большевицкого переворота партизаны в целом склоняются 
        к тому, чтобы «признать народных комиссаров за власть», но настоятельно 
        требуют продолжения войны, сохранения боеспособности армии, а также отказываются 
        участвовать в начинающейся междоусобице<sup>7</sup>. Это настолько противоречит 
        всей политике большевиков, что появление в следующем году Станислава Балаховича 
        под красным знаменем в должности командира конного полка выглядит странным 
        и необъяснимым. <br>
        <br>
        Своя логика тут, однако, есть. Балаховцы были одним из последних отрядов, 
        оказывавших сопротивление наступающим германским войскам вплоть до конца 
        февраля 1918 года. Оставшись с горсточкой соратников, среди которых главным 
        его помощником был младший брат Иосиф, Балахович просто вынужден был под 
        натиском немцев взаимодействовать с красными отрядами, а после тяжелого 
        ранения в начале марта - эвакуироваться для лечения вглубь советской России. 
        За это время ему удалось установить тайные контакты с русскими и польскими 
        антибольшевицкими кругами (попытки создания на советской территории польского 
        легиона завершились неудачей и стоили жизни некоторым их инициаторам<sup>8</sup>, 
        а часть легионеров нашла убежище в отряде Балаховича<sup>9</sup>), а также 
        с французской миссией. Приступив к формированию полка, «товариш Балахович» 
        (сам он предпочитает, чтобы его уважительно именовали «Батькой») одновременно 
        организует... крестьянские восстания против советской власти. <br>
        <br>
        Впрочем, вести двойную игру всегда сложно. Одной рукою инспирируя недовольство 
        крестьян, другою приходится, по требованиям красного командования, подавлять 
        их выступления. Психологическое напряжение среди балаховцев нарастало, 
        Булак уже обдумывал планы широкомасштабной крестьянской войны против большевиков 
        и подготовил воззвание: «Я дам вам оружие, дам храбрых начальников... 
        Нет силы, которая сможет сломить эту великую народную крестьянскую армию»<sup>10</sup>. 
        Но поступают сведения, что во Пскове, откуда уже готовятся уходить германские 
        оккупанты, формируются русские добровольческие части для борьбы против 
        большевиков, и «Батька Балахович», предпочтя службу в регулярной армии 
        сомнительным лаврам «народного вождя», с большинством подчиненных в ноябре 
        1918 г. прорвался во Псков и из красного командира стал белым офицером. 
        <br>
        <br>
        Правда, и в регулярной армии он оставался самим собой сторонником дерзких 
        партизанских операций и весьма своеобразной «партизанской» дисциплины. 
        Один из наиболее прославившихся военачальников Северо-Западной армии, 
        Булак-Балахович, произведенный в 1919 г. в генерал-майоры, быстро приобрел 
        как восторженных почитателей, так и влиятельных врагов. Действительные 
        и мнимые прегрешения, а также интриги недоброжелателей привели к тому, 
        что в августе 1919 г. Булак-Балахович был приказом генерала Юденича исключен 
        из рядов армии. Оставшись вновь лишь с горсточкой самых преданных партизан, 
        «Батька» перешел на территорию Эстонской Республики. <br>
        <br>
        Здесь Балахович заявил о поступлении на белорусскую службу и принял титул 
        командующего «Особым отрядом БНР в Прибалтике». Однако активных боевых 
        операций не предвидится, и после заключения эстонским правительством перемирия 
        с большевиками генерал склонился к мысли о переходе на новый театр военных 
        действий, где уже около года сражаются против красных польские войска. 
        <br>
        <br>
        Нельзя сказать, чтобы на «польском фронте» отряд Балаховича был принят 
        с распростертыми объятиями. Хватало и скептиков, позицию которых отражают 
        воспоминания К. Вендзягольского, бывшего в 1920 г. доверенным лицом маршала 
        Пилсудского и характеризовавшего балаховцев как «отъявленных псковских 
        головорезов, легендарно прославившихся на фронте войск генерала Юденича». 
        Впрочем, и Вендзягольский должен был признать за ними «совершенно исключительную, 
        почти фантастическую отвагу, предприимчивость и решимость», «ненависть 
        к большевикам не как к обыкновенному противнику, а как к личному заклятому 
        врагу»<sup>11</sup>. Находясь в оперативном подчинении польскому командованию, 
        но без включения в состав польской армии, полки Балаховича сражались под 
        бело-сине-красным русским флагом и с трехцветными кокардами напоминая, 
        что и на этом театре не прекращается не только польско-советская, но и 
        <em>русско-советская война... </em><br>
        <br>
        После заключения перемирия в октябре 1920 г. перед балаховцами, формирующими 
        отдельную союзную армию, открывается безрадостная перспектива интернирования. 
        Однако Булак и его соратники выбрали другой путь и 12-тысячная Русская 
        народная добровольческая армия в одиночку перешла в отчаянное и почти 
        безнадежное наступление (младший Балахович, в этом походе произведенный 
        братом в генералы, даже призывал, фактически бросив тылы, рваться прямиком 
        на Москву<sup>12</sup>). Но силы слишком неравны, и балаховцы, оставив 
        по себе на трехсотверстном пути от Турова до Гомеля память как солдатской 
        доблестью, так и «партизанскими» буйствами, после трех недель непрерывных 
        боев были вынуждены отойти на польскую территорию. <br>
        <br>
        Еще несколько лет вынашивались планы возобновления открытой борьбы. Не 
        считал борьбу оконченной и враг - в 1923 г. коммунистическими террористами 
        был убит Иосиф Балахович, и на Станислава также готовились покушения, 
        к счастью, не достигшие цели. Генерал, зачисленный в резерв польской армии, 
        старался поддерживать своих старых соратников и... ждал новой войны. «Да, 
        господа, скоро еще нам придется отвечать на вопрос: «во Христа веруешь?», 
        причем утвердительный ответ будет требовать немедленной защиты с оружием 
        в руках христианской цивилизации, - говорил он друзьям в 1938 году. - 
        Поверьте мне, вопрос будет так поставлен, или победить, или влачить жалкую 
        жизнь раба и лизать пятки большевицкого хама!»<sup>13</sup><br>
        <br>
        Первый удар был нанесен с запада, а не с востока; и, хотя генерал Балахович 
        9 сентября 1939 г. направлял своего эмиссара для формирования добровольческих 
        отрядов в Брест, Люблин и Вильну<sup>14</sup>, то есть в районы, которым 
        угрожало советское наступление, самому ему довелось участвовать в боевых 
        действиях против германских войск. Обстоятельствам участия Балаховича 
        и балаховцев в кампании 1939 г. и посвящен рапорт (памятная записка «Генерал 
        Станислав Булак-Балахович», датированная 16 сентября 1942 г.) майора Станислава 
        Славинского, предлагаемый вниманию читателей. Автор подчеркивает «исполнение 
        генералом Станиславом Булак-Балаховичем солдатского долга в его служении 
        Польше» и видит в его действиях в 1939 г. доказательство, «что он является 
        законным и праведным сыном Речи Посполитой»; в то же время следует отметить, 
        что на страницах рапорта фигурируют и люди, выполнявшие солдатский долг 
        в боях против немцев, не будучи связанными с Польшей своими корнями. <br>
        <br>
        Самой яркой фигурой после генерала Балаховича, наверное, в этом отношении 
        был полковник Станиславский. Говоря об участии этого офицера в гражданской 
        войне в России в 1920 г., майор Славинский называет его «28-летним подполковником 
        Николаем Шустровым-Станиславским, сочетавшим в одном лице сразу офицера 
        и флота, и авиации». По всей видимости, речь идет о Николае Николаевиче 
        Шустрове (до 25 февраля 1916 г. носил фамилию «Шуцман»). Он родился 12 
        января 1893 г., окончил Морской корпус в 1914 г., после чего служил на 
        Черном море, в том числе в гидроавиации<sup>15</sup>. О личной храбрости 
        Шустрова может свидетельствовать тот факт, что еще гардемарином, в 1913 
        г., он был награжден серебряной медалью «За спасение погибающих»<sup>16</sup>. 
        В годы гражданской войны Шустров участвовал в белом движении и даже был 
        одно время начальником штаба отряда Булак-Балаховича. Польский источник 
        1920 г. упоминает «полковника Николая Станиславского» (когда и при каких 
        обстоятельствах он принял эту фамилию, неясно), бывшего летчика, отмечая 
        его недостаточное военное образование и характеризуя весьма критически: 
        «горький пьяница и скандалист» («nałogowy pijak i awanturnik»)<sup>17</sup>. 
        Как и многие балаховцы, с негативными качествами Шуцман-Шустров-Станиславский, 
        очевидно, сочетал доблесть и самоотверженность, ярко проявившиеся в кампанию 
        1939 г. и подробно описанные в публикуемом документе. <br>
        <br>
        Другим чрезвычайно интересным свидетельством является рассказ о роли в 
        те дни полковника М. Яковлева, оставившего свой след и в гражданской войне 
        в России, и в польско-советской кампании 1920 г. Яковлев родился около 
        1891 г. и в 1917 г. был штабс-капитаном или капитаном; с конца 1918 г. 
        служил в «Волчанском (по названию города Волчанска) конно-партизанском 
        отряде», командовал им и неоднократно отличался в боях при наступлении 
        на Киев, а в начале 1920 г. в составе группы генерала Н.Э. Бредова отошел 
        на территорию, занятую польскими войсками. В 1920 г. командовал «Отдельной 
        сводно-казачьей бригадой», в отличие от Балаховича, настаивал на прямом 
        подчинении польскому командованию и был произведен в полковники, хотя 
        обстоятельства производства неясны<sup>18</sup>. До сих пор информация 
        об участии Яковлева в кампании 1939 г. ограничивалась отрывочными упоминаниями, 
        в которых он представлен помощником генерала Балаховича, - рапорт же майора 
        Славинского рисует более сложную картину взаимных конфликтов и несогласий.<br>
        <br>
        Начинающее рапорт краткое изложение биографии Балаховича, возможно, сохранившее 
        следы устных рассказов самого генерала, полно неточностей и прямых ошибок; 
        публикуемая же часть документа является исключительно ценным источником, 
        содержащим уникальную информацию и вписывающим новые яркие страницы в 
        историю жизни и борьбы Станислава Булак-Балаховича и некоторых из его 
        соратников.<br>
