        <span class="text"><strong>Александр Петрушин</strong><br>
        <br>
        <span class="header"><strong>ДВА КОМБРИГА</strong></span><br>
        <br>
        <em>&quot;Тюменский курьер&quot; № 113 (3130) от 29 июня 2011 г.</em><br>
        <br>
        <br>
        <strong><em>29 июня 1941 года Совет народных комиссаров СССР и ЦК ВКП(б) 
        направили в партийные комитеты прифронтовых областей директиву «Об организации 
        борьбы в тылу германских войск». Эта дата с прошлого года отмечается в 
        нашей стране как День партизан и подпольщиков.</em></strong><br>
        <strong><br>
        Ахтунг! Партизанен!</strong><br>
        <br>Тогда, 70 лет назад, 29 июня немцы, захватив Минск, завершили разгром 
          частей 3-й, 4-й и 10-й советских армий Западного фронта, сосредоточенных 
          перед войной на Белостокском выступе советско-германской границы. В 
          плен попали первые 330 тысяч бойцов и командиров Красной армии - к декабрю 
          первого года войны там окажется более 3,5 миллиона военнослужащих.<br>
        <br>Оставшиеся в тылу наступающих немецких войск красноармейцы, которым 
          посчастливилось избежать плена, стихийно сбивались в партизанские отряды. 
          Разочаровавшиеся в оккупантах как освободителях от большевиков, местные 
          жители стали помогать партизанам, пополняя их ряды и снабжая продовольствием. 
          Оружие и боеприпасы черпались партизанами из брошенного частями Красной 
          армии при отступлении.<br>
        <br>Поражение немцев под Москвой в декабре 1941-го способствовало развитию 
          партизанского движения, которое стало представлять для вермахта и оккупационной 
          администрации серьезную проблему.<br>
        <br>К тому времени Москва поставила первоначальную партизанскую вольницу 
          под свой контроль. Сначала партизанами руководили венные советы соответствующих 
          фронтов и подпольные обкомы оккупированных областей. 30 мая 1942 года 
          при Ставке Верховного главнокомандования был создан Центральный штаб 
          партизанского движения во главе с первым секретарем компартии Белоруссии 
          Пантелеймоном Кондратьевичем Пономаренко.<br>
        <br>Значительную роль в активизации партизанского движения сыграли оперативно-боевые 
          группы НКВД СССР общей численностью более 15 тысяч человек. Одной из 
          таких групп командовал капитан госбезопасности Иван Филиппович Титков.<br>
        <br>
        <strong>Сельхозшкола НКВД</strong><br>
        <br>Титков родился в 1912 году в деревне Слобода Белевского района Тульской 
          области в крестьянской семье. Когда родители переехали в Тулу, сын работал 
          на строительстве фабрики-кухни, заочно окончил строительный техникум, 
          служил в войсках НКВД. В 1936 году его по примеру известного педагога 
          Антона Семеновича Макаренко, автора «Педагогическая поэма» (1935), назначили 
          заведующим сельскохозяйственной воспитательной школой, организованной 
          в селе Кукушкино Исетского района Омской области для детей, чьи родители 
          были репрессированы в 1937-1938 годах. Эта школа просуществовала недолго: 
          повзрослевших воспитанников перевели в другие колонии, а Титкова использовали 
          на различных руководящих должностях в горнодобывающей промышленности 
          Кузбасса, где работали заключенные ГУЛАГа. Когда началась война, он 
          стал готовить диверсантов-подрывников, а в июне 1942-го самого направили 
          в тыл врага - Минскую область, где действовал партизанский отряд имени 
          Железняка.<br>
        <br>Главной целью этого отряда, развернутого в бригаду «Железняк», стала 
          сформированная 1-я русская национальная бригада «За Русь». Этим соединением 
          командовал бывший подполковник Красной армии Владимир Владимирович Гиль-Родионов.<br>
        <strong><br>
        Изменник Родины или ее спецагент?</strong><br>
        <br>Гиль родился в 1906 году в г. Вилейка (Царство Польское) в рабочей 
          семье. Работал на железной дороге. С 1926 года - в Красной армии, окончил 
          Борисоглебскую кавалерийскую школу (1929) и Военную академию имени Фрунзе 
          (1940). Его называли «самым блестящим командиром», в марте 1941-го года 
          он возглавил штаб 229й стрелковой дивизии (первого формирования). Во 
          многом благодаря его военным знаниям и личной смелости эта дивизия успешно 
          сдерживала наступление противника в Белоруссии.<br>
        <br>Но в июле 1941-го раненый в бою возле г. Толочин Гиль оказался во вражеском 
          плену. Содержался в офлаге N 68 в Сувалках, где согласился на сотрудничество 
          с гитлеровцами.<br>
        <br>Под фамилией «Родионов», чтобы его не принимали за немца, он сформировал 
          из советских военнопленных лжепартизанский отряд, выросший вскоре в 
          1-ю русскую национальную бригаду «За Русь» численностью до трех тысяч 
          человек. Бригада включала в себя три строевых и один учебный батальон, 
          пулеметную, автомобильную, штурмовую роты, батареи орудий и минометов, 
          два кавалерийских, саперный, комендантский взводы и хозяйственные подразделения. 
          Личный состав был одет в чешскую военную форму с погонами собственного 
          образца и черной лентой на обшлагах мундиров с надписью «За Русь!» На 
          вооружении бригады имелось пять орудий, 20 минометов, 280 пулеметов, 
          винтовки советского, немецкого и чешского производства.<br>
        <br>Начальником разведки Гиль-Родионов назначил бывшего командира 48-й 
          стрелковой дивизии генерал-майора Павла Богданова, сдавшегося в плен 
          17 июля 1941 года. Он выдал немцам политработников своей дивизии (их 
          расстреляли) и написал два воззвания, переданных по радио - «К русскому 
          народу» и «К генералам Красной армии» (в последнем он отказался от звания 
          генерала и советского гражданства).<br>
        <br>Хорошо вооруженная и дисциплинированная бригада Гиль-Родионова вела 
          с переменным успехом бои против партизан в Белоруссии. Разные свидетельства 
          отмечают жестокость самого Гиля. В частности, один из белорусских эмигрантов 
          Юрий Дувалич утверждал:<br>
        <br>«В местечке Зембин им были убиты три юноши и две девушки только за 
          то, что они пришпилили к своим рубашкам и блузам белорусский национальный 
          значок». В деревне Слобода Гиль обещал осужденным им к расстрелу крестьянам 
          помилование, если они обратятся к нему на русском литературном языке. 
          Из 147 деревень Бегомльского района после Родионова осталось только 
          девять. Думается, что с самого начала Родионов был советским провокатором. 
          Характерно, что он всячески избегал столкновений с большевистским партизанами, 
          а занимался почти поголовным уничтожением мирного населения».<br>
        <br>Один из бойцов бригады «За Русь», осужденный после войны на 25 лет, 
          также утверждал, что Гиль еще в период пребывания в немецком лагере 
          военнопленных в Сувалках вынашивал замысел провоцировать ненависть к 
          оккупантам при помощи расправ над местным населением.<br>
        <br>Некоторые историки считают, что Гиль до войны сотрудничал с органами 
          НКВД, а направление Титкова в район дислокации бригады «За Русь» вызвано 
          стремлением руководства НКВД не допустить формирования на ее базе «Русской 
          освободительной армии» под командованием бывшего командующего 2-й Ударной 
          армии генерал-лейтенанта Андрея Власова.<br>
        <br>10 августа 1943-го года Титков конспиративно встретился с Гилем в деревне 
          Будиловка. Договорились об «искуплении вины кровью» путем «перехода 
          бригады на сторону партизан», ликвидации немецких инструкторов и выдачи 
          генерала-предателя Богданова.<br>
        <br>Любопытно, что тайному свиданию двух комбригов предшествовала довольно 
          продолжительная переписка в духе картины Репина «Запорожцы пишут письмо 
          турецкому султану». Содержание одной из записок от Родионова таково: 
          «Бандиты! Переходите на нашу сторону. Водкой напою! Хлебом накормлю!» 
          Ответ Титкова на обратной стороне этого послания: «Советские войска 
          наступают. А куда вы, предатели, побежите? Под землей найдем и заставим 
          держать ответ за все ваши зверства».<br>
        <br>13 августа родионовцы перебили в своей бригаде всех немцев и перешли 
          на сторону партизан. Богданова отправили на самолете в Москву в Главное 
          управление военной контрразведки «СМЕРШ», а 24 апреля 1950 года расстреляли.<br>
        <br>Через три дня комбриг Титков передал комбригу Гилю-Родионову радиотелеграмму, 
          полученную из Москвы: «Поздравляю с переходом на сторону партизан для 
          активных действий против немецких захватчиков. Утверждаю наименование 
          бригады «1-я Антифашистская», а вас - командиром. Пономаренко».<br>
        <br>
        <strong>Кавалер ордена Сталина</strong><br>
        <br>Переход на сторону партизан сформированного гитлеровцами из советских 
          военнопленных достаточно крупного и боеспособного соединения не остался 
          незамеченным ни для Гитлера, ни для Сталина.<br>
        <br>Фюрер заявил в бешенстве на одном из совещаний высшего командования: 
          «Мне вообще генерал Власов не нужен… Мы не можем после измены Гиля поручить 
          эти части третьему лицу, который их заберет в руки и скажет: сегодня 
          мы идем вместе, а завтра - нет…»<br>
        <br>По утверждению шефа политической разведки Третьего рейха Вальтера Шелленберга, 
          «Родионов вылетел с секретного партизанского аэродрома в Москву. Там 
          он был принят лично Сталиным и награжден орденом Сталина».<br>
        <br>К тому времени в немецком плену находилось около 4 миллионов бойцов 
          и командиров Красной армии (всего в войну сдалось и было захвачено 5 
          миллионов 734 тысячи 528 военнослужащих). Заманчиво было использовать 
          в своих целях оказавшуюся в тылу неприятеля огромную силу военнопленных. 
          Но прежде их надо было публично простить, покаявшись в своих собственных 
          ошибках и представив пленение (во всех формах - добровольное и вынужденное) 
          досадным недоразумением. Награждение командира первого формирования 
          военнопленных, выступивших против немцев, орденом Сталина Шелленберг 
          принял за начало реабилитации. И в очередной раз ошибся. Сталин считал 
          предателями, независимо от обстоятельств пленения, всех военнопленных 
          со всеми вытекающими для них и их семей трагическими последствиями (практика 
          таких репрессий была отменена только в июле 1956 года правительственным 
          постановлением «Об устранении последствий грубых нарушений законности 
          в отношении бывших военнопленных»).<br>
        <br>Ордена Сталина в советской наградной системе не существовало, хотя 
          окружение вождя неоднократно предлагало учредить такую награду по аналогии 
          с орденом Ленина. Сталин, несмотря на внешне выраженную марксистско-ленинскую 
          убежденность, был суеверен и мистически опасался своего увековечивания 
          в орденах. Другое дело - совмещение его имени и изображения с символами 
          Победы. Поэтому описание ордена Сталина изменили: вместо профиля вождя 
          в центре платиновой звезды поместили изображение Кремля - так 8 ноября 
          1943 года этот наградной знак стал орденом Победы.<br>
        <br>Гиль-Родионов получил в Кремле из рук Председателя Президиума Верховного 
          Совета СССР Калинина орден Красной Звезды, а Сталин подарил ему новый 
          автомат ППШ, с которым командир «1-й Антифашистской бригады» возвратился 
          в тыл противника. Майору Титкову «за особые заслуги в развитии партизанского 
          движения в Белоруссии» было присвоено 1 января 1944 года звание Героя 
          Советского Союза. Специалисты разведки расценивают как блестящую операцию 
          «срыв намерений фашистской Германии организовать на базе бригады Гиль-Родионова 
          Русскую освободительную армию Власова». Два комбрига предотвратили возможность 
          полномасштабной гражданской войны.<br>
        <br>Стремясь доказать свою преданность советской власти, Гиль-Родионов 
          бросал свою бригаду на самые дерзкие операции. Такой стало нападение 
          на его родной город Вилейку в ночь на 1 ноября 1943 года.<br>
        <br>Весной следующего года немецкое командование предприняло наиболее крупное 
          за всю войну наступление против партизан Полоцко-Лепельской зоны. В 
          нем участвовало более 60 тысяч солдат и офицеров, бронетехника, артиллерия 
          и авиация. Силы были неравные: при прорыве вражеской блокады «партизанского 
          края» у села, Ушачи Витебской области 14 мая 1944 года полковник Гиль-Родионов 
          был смертельно ранен. Его бригада потеряла 1026 человек (больше половины 
          личного состава) и фактически перестала существовать.<br>
        <br>Майор Титков после освобождения Белоруссии служил в органах госбезопасности, 
          работал управляющим строительным трестом в городе Гурьеве - ныне Атырау 
          Республики Казахстан. Скончался 23 июля 1982 года в Минске, там похоронен.<br>
        <br>При жизни его спрашивали: «Вы общались с Родионовым. Какое впечатление 
          сложилось о нем - как о предателе? Или это была трагедия человека, проявившего 
          слабость в тяжкую минуту?» Титков задумался: «Гиль-Родионов был, прежде 
          всего, хорошо подготовленным военным. Решение о переходе к партизанам 
          выглядело искренним. Он перешел, чтобы помочь России, как он говорил, 
          освободиться от оккупантов. И очень хорошо воевал с немцами. Попав в 
          плен, скорее всего, проявил слабость, но вскоре понял, на какой путь 
          вступил. Что было бы с ним после войны, останься он в живых? Не знаю. 
          Но не думаю, что он оказался бы в лагерях… Вину свою он искупил…»<br>
