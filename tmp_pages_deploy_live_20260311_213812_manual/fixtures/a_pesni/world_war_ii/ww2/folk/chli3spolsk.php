        <span class="text"> <span class="header"><strong>ШЛИ ТРИ ГЕРОЯ С ПОЛЬСКОГО 
        БОЯ</strong> <br>
        </span> <br>
        Шли три героя с польского боя,<br>
        С польского боя домой.<br>
        Только они вышли на финску границу,<br>
        По ним финн ударил три раза. <br>
        <br>
        «Ударил, ударил, он грудь мою ранил,<br>
        Он грудь мою ранил тяжело. <br>
        <br>
        Товарищ, товарищ, болят мои раны,<br>
        Болят мои раны глубоко.<br>
        Одна заживает, вторая заживает,<br>
        А с третьей придется помереть. <br>
        <br>
        А дома детишки, жена молодая,<br>
        Они ж меня будут ожидать.<br>
        Пусть дожидают, ведь ихнее дело,<br>
        Они не дождутся никогда. <br>
        <br>
        Товарищ, товарищ, зарой мое тело,<br>
        Зарой мое тело глубоко.<br>
        Пусть финны узнают, пускай вспоминают,<br>
        Что был я героем на войне. <br>
        <br>
        И вырастут дети и спросят мамашу:<br>
        &quot;Где наш родимый отец?&quot;<br>
        А мать отвернется, слезами зальется:<br>
        &quot;Отец твой убитый на войне. <br>
        <br>
        Убитый, убитый, ничем не прикрытый<br>
        Лежит на сырой на земле.<br>
        Его уже тело совсем почернело,<br>
        И птицы все кости разнесли&quot;. <br>
        <br>
        <em>С сайта <a href="http://www.unn.ru/rus/f9/old/fond/index.html" target="_blank">Фольклорного 
        фонда им. проф. В. Н. Морохина</a> </em><br>
        <br>
        <br>
        Существует множество трагикомических военных и блатных песенок на этот 
        же мотив со сходным сюжетом: героя, который возвращается с войны или бежит 
        из тюрьмы, смертельно ранят враги (поляки, финны, чекисты), и он произносит 
        своему товарищу монолог со словами &quot;Товарищ, товарищ, болят мои раны…&quot;. 
        Либо монолог произносится в лазарете медсестре. Песенки эти известны с 
        дореволюционных времен, и продолжали сочиняться в годы советской власти. 
        Самая известная из них - <a href="../../dvor/sodesskogo.php">&quot;С Одесского кичмана&quot;</a>. 
        Основой сюжета могла послужить песня на стихотворение Михаила Михайлова 
        <a href="../../popular20/grenadery.htm">&quot;Из Франции два гренадера&quot;</a> 
        (перевод из Генриха Гейне, опубликован в 1846 году).<br>
        <br>
        <br>
        В данной версии скорее всего имеется в виду короткая &quot;победоносная&quot; 
        польская кампания сентября 1939 года, вскоре после которой - 30 ноября 
        - началась тяжелая финская война, которая обошлась в 127 тысяч убитых 
        с советской стороны и 48 тысяч - с финской.<br>
        <br>
        Под &quot;польским боем&quot; могут также иметься в виду бои в Польше 
        с немцами в конце Первой мировой войны (см. песню <a href="../../ww1/kakvarchavu.php">&quot;Как 
        Варшаву мы сдавали&quot;</a>) либо советско-польская война 1919-1920 гг.; 
        кроме того, тогда наш герой каким-то образом участвует в конфликтах на 
        финской границе (по Брестскому миру Финляндия отошла к Германии, а после 
        его денонсации осталась независимой, с чем большевики не могли смириться, 
        однако мир между Финляндией и большевиками подписан только 14 октября 1920 года). 
        <br>
        <br>
        <br>
        <strong>Советско-польская война 1919-1920 гг.</strong><br>
        <br>
        В ноябре 1918 года завершилась Первая мировая война: 3 ноября капитулировала 
        (а вскоре распалась) Австро-Венгрия, 11 ноября - Германия. Обе вывели 
        свои войска с оккупированных территорий. Польша 11 ноября объявила независимость, 
        то же самое сделала Галиция, провозгласив Западно-Украинскую народную 
        республику. Украина объявляла независимость еще при германской оккупации, 
        а теперь там шла Гражданская война и действовало несколько правительств, 
        как и в Советской России. Все перечисленные выше государственные образования 
        имели разные взгляды на расстановку границ, и проблема была решена военным 
        путем. В итоге, когда всё закончилось, на карте остались лишь Польша (ей 
        досталась Галиция) и Советская Россия, подчинившая себе Украину.<br>
        <br>
        Советско-польская война длилась 20 месяцев. Первое боевое столкновение 
        РККА с польскими войсками произошло в феврале 1919 года на литовско-белорусской 
        территории. Летом 1919 года поляки разбили и вытеснили на Восточную Украину 
        Галицийскую армию, и Западно-Украинская республика была ликвидирована. 
        В феврале 1920 года Галицийская Армия перешла на сторону Красной Армии, 
        влившись в ее ряды. <br>
        <br>
        Самые яркие события развернулись в 1920 году. Заключив союз с украинским 
        правительством Петлюры, польская армия весной 1920 года вошла в Житомир 
        и Киев. В это же время, в мае 1920 года, большевики замкнули Врангеля 
        в Крыму и двинули Красную Армию на Запад для ускорения мировой революции. 
        Армия дошла до Варшавы.<br>
        <br>
        Поляки окопались вдоль Вислы. 15 августа 1920 года, в праздник Девы Марии, 
        конница Юзефа Пилсудского вошла в тыл красным и в крупнейшем в истории 
        кавалерийском сражении разнесла идею экспорта революции в пух и прах. 
        Европа назвала это «Чудом на Висле». На 19 лет Польша обрела независимость. 
        Позже Тухачевский «отыгрался» на подавлении Кронштадта и Антоновщины. 
        <br>
        <br>
        18 марта 1921 года был подписан советско-польский Рижский договор, определивший 
        границы. Спорные украинско-белорусские территории (в польской терминологии 
        - &quot;кр<strong>е</strong>сы&quot;, в российской - &quot;Западная Украина 
        и Западная Белоруссия&quot;) остались за Польшей, но идеи самостийности 
        в них не угасли и вылились впоследствии в бандеровское движение.<br>
        <br>
        <br>
        <strong>Польская кампания 1939 года</strong><br>
        <br>
        23 августа 1939 года СССР и Германия заключили Договор о ненападении (так 
        называемый пакт Молотова-Рибентроппа), поделив между собой Восточную Европу. 
        Советскому Союзу достались Прибалтика, Молдавия и Правобережная Польша 
        (она же Западная Украина и Западная Белоруссия). В соответствии с пактом 
        СССР и Германия совместно атаковали Польшу (1 сентября напала Германия, 
        17 сентября - СССР). 22 сентября армии агрессоров устроили парад победителей 
        в городе Бжешч (бывший Боест-Литовск и современный белорусский Брест). 
        Для Советского Союза эта война обошлась практически без потерь, так как 
         польским войскам на восточном фронте приказано было не оказывать сопротивления ввиду его бесполезности.<br>
