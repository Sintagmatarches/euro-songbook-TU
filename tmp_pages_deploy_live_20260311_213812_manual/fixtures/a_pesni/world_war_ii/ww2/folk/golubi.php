 <br> <br>
Песня о 16-летнем ученике ремесленного училища Викторе Черевичкине (по другой версии - Черевичном), расстрелянном гитлеровцами 28 ноября 1941 г., во время недельной оккупации Ростова-на-Дону. Наутро Красная армия отбила город. Виктор жил на 28-й линии, в районе парка им. Фрунзе, где проходил рубеж обороны, и держал почтовых голубей, что было запрещено оккупационными властями. Перед расстрелом он выпустил голубей на волю. Виктору установлен  в 1961 г. памятник в ростовском парке,  сам парк и одна из городских улиц названы его именем. Фотография расстрелянного подростка, сжимающего голубя, сделанная военным корреспондентом Максом Альпертом фигурировала на Нюрнбергском процессе: 
<br><br>
<img src="cherevichkinvitya01.jpg" width="500" height="345" alt="Витя Чкревичнкин (Черевичный)"/><br>
<br>
<br>
Песня поется на  мелодию танго <a href="../../cabaret/pustdni.php">&quot;Пусть 
  дни проходят&quot;</a> (слова Ильи Финка, музыка Бориса Терентьева, 1938; на эту же мелодию создана <a href="baksanskaja.php">"Баксанская"</a>). Появилась не позднее весны 1944 г. - Людмила Гурченко вспоминала, как разучивала ее в пионерлагере &quot;Райеленовка&quot; под Харьковым в начале лета 1944 г.:<br>
<br>
<em>Кормили здесь четыре раза в день! Но все время хотелось есть. Сахар давали один раз - утром. Мы по своему усмотрению распределяли его на весь день. Сахар все носили с собой... никому и в голову не приходило, что его можно оставить на столе. До ужина сахар у меня не доживал никогда.<br>
<br>
Была в пионерлагере и музыка. Аккордеонистка Дина Печенежская, дочь папиного товарища, Андрея Степановича Печенежского, вела у нас художественную самоеятельность.<br>
<br>
В доме Печенежских был траур: &quot;вторые немцы&quot; <em>[т.е. при второй оккупации Харькова, более жестокой - прим. a-pesni] </em>расстреляли их семнадцатилетнего Николая - любимого сына и прекрасного баяниста, который &quot;ще ребенком быв, в вже играв, як зверь&quot;.<br>
<br>
И теперь Дина, его сестра, тоже играла &quot;як зверь&quot; и разучивала со мной песни с &quot;жестикуляцией&quot;, с увлечением проводила надо мной эксперименты.<br>
<br>
Первым моим номером с &quot;жестикуляцией&quot; была песня про Витю Черевичкина - слова неизвестного автора на музыку песни Терентьева &quot;Пусть дни проходят&quot;. Для этого нужно было вместе с первыми строками:<br>
<br>
Жил в Ростове Витя Черевичкин - <br>
<br>
согнуть правую руку в локте, выбросить ее чуть в сторону; взгляд - к зрителям, сидящим справа. На вторую строчку:<br>
<br>
В школе он отлично успевал -<br>
<br>
все то же самое, но - в левую сторону. Дальше просто поешь:<br>
<br>
И в свободный час всегда обычно<br>
Голубей любимых выпускал.<br>
<br>
На припеве и голова и руки постепенно устремляются к небу:<br>
<br>
Голуби, мои вы милые,<br>
Улетайте в облачную высь...<br>
<br>
Дина меня наставляла:<br>
<br>
- Люся! Во втором куплете война. Лицо жесткое, руки сжаты в кулаки, хорошо, если в глазах заблестит слеза - это всегда впечатляет. И финал - победа! Лицо радостное, торжественное! А руки - широко-широко! Как будто хочешь обнять мир! Ну!<br>
<br>
Но недолго эти дни тянулись,<br>
И, разбив фашистских подлых псов,<br>
