	В основе -
<a href="../../drugije/dunajevskij/marchvesel.php">&quot;Марш веселых ребят&quot;</a> Исаака Дунаевского и Василия Лебедева-Кумача из фильма &quot;Веселые ребята&quot; (1934, там же см. ноты). Песня из воспоминаний ленинградского фольклориста Владимира Бахтина (1923-2001) о блокаде. Во время блокады он закончил школу и пошел на фронт защищать Ленинград:<br>
<br>
&quot;Я пошел в школу в сентябре, потом нас распустили, мы дежурили на крыше в школе, директор и мы по очереди — человека три-четыре-пять. Пока не было еще такого страшного голода, мы веселились. Играли в подкидного дурака. Сидели там в помещении под крышей, у нас всегда была большая трехлитровая банка с водой и сода. И лимонная кислота иногда бывала. И кто проигрывал в подкидного, тот стакан шипучки выпивал, из воды делали. Это была наша забава такая. Затем все это прекратилось на несколько месяцев, и где-то весной, в апреле или в мае, нас собрали. И уже не сто человек, а шестнадцать. Вот у меня список сохранился, у меня тетрадь есть, где я записывал список обедающих. Мы приходили, у каждого на боку висела сумка из-под противогаза. Дорский, Дыскина, Каминская, Бородин, Муляр, Сумкина. Верочка. Грибкова, Семенова, Акутина, Павлова, Церековский, Гурвич, Тамаркин... Живы четверо из этих шестнадцати. Включая меня. Вынимали ложку и шли в столовую. В столовой, я не преувеличиваю ничего, давали именно столовую ложку каши, сделанной из муки. Мы съедали и шли на занятия. Учителя дистрофики тоже были. Удивительные люди. Иногда мы ездили в совхоз пригородный. Ленинград в блокаде был, но вот в Отрадное там, поближе, там еще были наши, и мы пололи, это уже летом. И когда мы кончили школу, то нас собрали во Дворце пионеров, очень торжественный акт был, нам дали по трети или четверти стакана красного вина, по полстакана кофе и по пирожному. Это было большое торжество такое&quot;.<br>
