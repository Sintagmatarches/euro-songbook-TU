    двадцать,<br>
    тридцать, <br>
    орок восемь, <br>
    может, больше, <br>
    я не помню, –<br>
    Лежу с оторванной ногой – притворился. <br>
    <br>
    Ко мне подходит санитарка – звать Тамарка. -<br>
    - Давай, - грит, - рану первяжу – сикось-накось, -<br>
    И в санитарную машину – студебеккер, <br>
    трехмоторный, <br>
    бесприцепный,<br>
    стекла биты, <br>
    шины рваны -<br>
    С собою рядом положу – только на ночь! <br>
    <br>
    С тех пор прошло годов немало.<br>
    В колхозе сторожем служу – не тужу.<br>
    Рощу картошку-скороспелку – поросятам,<br>
    Жену Тамарку сторожу – от соседа и туристов.<br>
