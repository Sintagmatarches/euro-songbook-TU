<h1>НОВОБРАНЦЫ</h1>

С деревьев листья облетают<br>
(прямо наземь),<br>
Пришла осенняя пора,<br>
(ёксель-моксель).<br>
Ребят всех в армию забрали<br>
(хулиганов),<br>
Настала очередь моя <br>
(главаря).<br>
    <br>
И вот приносят мне повестку<br>
(на бумаге, на газетной)<br>
Явиться в райвоенкомат <br>
(в восемь тридцать, можно позже,<br>
с сухарями, кружка сбоку).<br>
Маманя в обморок упала <br>
(с печки на пол, вверх ногами),<br>
Сестра сметану пролила<br>
(тоже на пол. Вот растяпа!)<br>
    <br>
Друзья, Маманю подсадите<br>
(взад на печку, как и было),<br>
Сестра, сметану подлижи<br>
(языком и снова в крынку).<br>
А я, молоденький парнишка<br>
(лет семнадцать, двадцать, тридцать,<br>
может, больше, я не помню),<br>
На фронт германский подался<br>
(прямо с места и в кальсонах).<br>
    <br>
За мною вслед бежит Аксинья<br>
(жопа толста, морда синя)<br>
В больших кирзовых сапогах<br>
(на босу ногу, чтоб не жали).<br>
За нею следом Афанасий<br>
(семь на восемь, восемь на семь)<br>
С большим спидометром в руках<br>
(скорость мерять).<br>
    <br>
Сижу в окопе неглубоком<br>
(метров восемь, с половиной, пули свищут),<br>
Подходит ротный командир<br>
(морда клином, звали Климом).<br>
- Здорово, братцы-новобранцы<br>
(матерь вашу, вашу также)!<br>
Сейчас в атаку побежим<br>
(ровно прямо, и налево, и обратно)!<br>
    <br>
Летят по небу самолеты<br>
(бомбовозы),<br>
Хотят засыпать нас землей<br>
(черноземом, и навозом, с головою),<br>
А я, молоденький мальчишка<br>
(лет семнадцать, двадцать, <br>
тридцать, может, сорок),<br>
Лежу с оторванной ногой<br>
(для маскировки, челюсть рядом, жрать охота).<br>
    <br>
Ко мне подходит санитарка<br>
(звать Тамарка,<br>
может, Дунька, может, Клавка,<br>
я не помню):<br>
Давай я рану первяжу<br>
(сикось-накось, кось-на-сикось)<br>
И в санитарную машину «Студебеккер»<br>
(стекла биты, шины рваны, <br>
шесть цилиндров,<br>
было восемь – два украли)<br>
С собою рядом положу<br>
(для интересу, я не против,<br>
только сзади, чтоб не дуло).<br>
    <br>
С тех пор прошло годов немало<br>
(лет семнадцать, двадцать,<br>
двадцать, тридцать, может, сорок),<br>
В колхозе сторожем служу<br>
(не тужу).<br>
Ращу картошку-скороспелку<br>
(поросятам, людям тоже),<br>
Жену Тамарку сторожу<br>
(от соседа и туристов, чтоб не съели,<br>
а ведь могут, в наше время).<br>
    <br>
На юг вороны полетели<br>
(ёксель-моксель!),<br>
Пришла осенняя пора…<br>
