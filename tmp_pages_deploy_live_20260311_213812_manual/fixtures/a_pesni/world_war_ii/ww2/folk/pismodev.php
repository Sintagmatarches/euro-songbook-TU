 <h1>ПИСЬМО 
  ДЕВУШКИ ИЗ НЕМЕЦКОГО ПЛЕНА </h1>

  Далекий мой, пора моя настала,<br>
  Последний раз я карандаш возьму,<br>
  К кому б моя записка ни попала,<br>
  Она тебе писалась одному.<br>
  <br>
  Что может дать отвергнутой рабыне<br>
  Чугунная немецкая земля?<br>
  И может, на какой-нибудь осине<br>
  Уже готова для меня петля.<br>
  <br>
  А может, мне валяться под откосом<br>
  С разбитой грудью у чужих дорог,<br>
  И по моим, по шелковистым косам<br>
  Пройдет немецкий кованый сапог.<br>
  <br>
  Прости, прощай, забудь про эти косы,<br>
  Они мертвы, им больше не расти.<br>
  Забудь калину, на калине росы,<br>
  Про все забудь, но только – отомсти!<br>
  <br>
  <span class='i'>В нашу гавань заходили корабли. Вып. 5. М.: Стрекоза, 2001.<br>
  </span><br>
  <br>
  Фольклоризованный вариант стихотворения Михаила Исаковского &quot;Прощальная&quot; 
  (Далекий мой! Пора моя настала..., 1942, см. ниже). Есть переработка ГУЛАГа 
  - <a href="../../dvor/spojemzigan.php">&quot;Споем, жиган, нам не гулять по бану…&quot;</a> Исполнялась на мелодию <a href="../oficial/marchtankist.htm">"Марша  танкистов"</a> из фильма &quot;Трактористы&quot; (муз. Дм. и Дан. Покрассов), и, возможно, на другие мелодии - например, благодаря сходству первой строки, вполне могли петь на <a href="../oficial/lubimgorod.php">"Любимый город"</a> из фильма 1939 г. &quot;Истребители&quot;(муз. Н. Богословского).<br>
