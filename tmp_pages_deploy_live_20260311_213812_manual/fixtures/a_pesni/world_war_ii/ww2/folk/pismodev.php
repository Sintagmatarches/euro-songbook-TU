<span class='i'>Прислал Виктор Александрович Костров 15.06.2018, с прим.:<br>
<br>
09 июня 2018 г. скончалась моя мама, Кострова Пелагея Ивановна. Вот черновик её неотправленного письма:<br>
<br>
"Уважаемая редакция! Пишу Вам первый раз по случаю приближающейся даты 65-летия Победы. Этот великий праздник я провожу в слезах, всех жалко и своё детство. Мне 79 лет. Когда война началась, мне было 10 лет. В 1941 году я должна была идти в 3-й класс, но начались бомбежки, мы жили в пос. Шаликово Можайского р-на Московск. обл. Это 98 км от Москвы. В октябре нас оккупировали немцы, а 13 января 42 г. их выбили наши войска. Немцы сожгли весь поселок, а людей, по морозу, по Минскому шоссе погнали на Смоленск. Моей маме было 34 года, нас у неё 3 девчонки 10 - 8 - 1 год, отец на фронте. Когда нас освободили войска, была огромная радость, мы вернулись в свой поселок, там одни трубы, поселились в овощехранилище в закроме, рядом были много других семей. Темно, сыро, холодно, но не одни. А летом я пошла работать на торфяное болото  в 11 лет, за 5 км ходила, да лесом по узкоколейке, но я получала 700 гр хлеба + 300 гр за перевыполнение нормы (мы носили и сушили торф). Носила котелок с собой, в обед съедала суп, а вечером несла его домой. Мама получала 500 гр хлеба, и мы все выжили, и сейчас живы, кроме мамы.<br>
 <br>
И в 1942 г. летом рядом были военные, и один молодой лейтенант по фамилии Ваничкин играл на гитаре и пел песню, которую мы с сестрой Аней запомнили и хотелось бы, чтоб она не пропала. Возможно её ещё кто-нибудь знает."<br>
