<br>
Музыка Дмитрия и Даниила Покрасс <br>
Слова Василия Лебедева-Кумача<br>
<br>
Три брата с фашистом дерутся жестоко, <br>
И каждый из братьев силен и плечист: <br>
Один - пехотинец, другой - смелый сокол, <br>
А третий - отважный танкист! <br>
<br>
Вот первый у речки ведет переправу, <br>
Приказ выполняя, торопит бойцов - <br>
И видит, что подлая вражья орава <br>
Забрать его хочет в кольцо. <br>
<br>
Враги навалились и справа, и слева, <br>
Их вчетверо больше, чем наших ребят. <br>
Но, полный великой отваги и гнева, <br>
Геройски дерется отряд. <br>
<br>
Фашистские гады кладут все усилья, <br>
Все жарче и жарче становится бой... <br>
Но вот засверкали знакомые крылья, <br>
И брат появился второй. <br>
<br>
Покончивши в небе с фашистскою стервой,<br>
Ударил герой по наземным врагам, <br>
И, братскую помощь почуявши, первый <br>
Дал волю геройским штыкам! <br>
<br>
Попятился враг, замешался - и встретил <br>
Конец свой проклятый фашист... <br>
На помощь двум братьям явился и третий, <br>
Явился отважный танкист. <br>
<br>
Так братья дерутся в содружестве смелом, <br>
Геройски воюют за Родину-мать, <br>
Чтоб гадам-фашистам, зверям оголтелым, <br>
По нашей земле не гулять. <br>
<br>
1941 <br>
