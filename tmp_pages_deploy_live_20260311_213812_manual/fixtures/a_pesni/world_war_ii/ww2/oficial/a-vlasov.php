	  <strong>Анатолий Марагин, историк </strong><br>
  <br>
 <span class="header"> <strong>ПЛАСТИЛИНОВАЯ СОВЕСТЬ ГЕНЕРАЛА ВЛАСОВА</strong><br></span>
  <br>
  <em>«Аргументы и Факты». 27 сентября – 3 октября 2006. №39 (1352). </em><br>
  <br>
  <br>
  <em><strong>В каком-то смысле генералу Андрею Власову повезло. Ему, единственному 
  из всех военачальников, удалось послужить двум диктаторам XX века – Сталину 
  и Гитлеру.</strong></em><br>
  <br>
  Причём на этом Власов останавливаться не собирался - в 1945 г. он предложил 
  свои услуги американцам. Как получилось, что советский генерал с лёгкостью стал 
  генералом вермахта? <br>
  <br>
  Когда после Гражданской войны большевики отменили такой пережиток прошлого, 
  как Бог, его заменили на так называемую &quot;революционную целесообразность&quot;. 
  Модифицированный человек был удобен в управлении. Он с одинаковым энтузиазмом 
  мог поддерживать Троцкого и бороться с троцкизмом, клеймить фашистскую Германию 
  и дружить с ней. Увы, имелся и побочный эффект. У идеальных продуктов советского 
  строя такая субстанция, как совесть, не то чтобы отсутствовала, но была исключительно 
  пластичной. Генерал Власов в этом смысле был близок к идеалу. <br>
  <br>
  Когда началась война, он командовал 4-м механизированным корпусом на Украине, 
  потом безуспешно оборонял Киев. Удача пришла к Власову в декабре 1941 г. под 
  Москвой. Его войска взяли Солнечногорск, фотография Власова появилась на первой 
  полосе &quot;Известий&quot;, сам Сталин принял его в Кремле, где ему вручили 
  второй орден... <br>
  <br>
  Обстоятельства изменились, когда Власову в январе 1942 г. приказали прорвать 
  блокаду Ленинграда. Наступление окончилось трагедией, 2-я ударная армия почти 
  полностью погибла у новгородской деревни Мясной Бор. А Власов сдался немцам, 
  после чего с ним произошла поразительная перемена! Этот &quot;сталинский сокол&quot; 
  вдруг вспомнил про миллионы узников ГУЛАГа, про раскрестьяненное крестьянство 
  и расказаченное казачество, про выбитые в НКВД зубы коллег-генералов... К каким 
  замечательным идеалам он призывал в листовках, которые немецкие самолёты сбрасывали 
  над окопами Красной армии! Свержение сталинской тирании, свобода слова и вероисповедания, 
  освобождение политзаключённых... <br>
  <br>
  Немцы далеко не сразу поверили в столь радикальную метаморфозу. Первую дивизию 
  Русской освободительной армии (РОА) ему разрешили сформировать только в сентябре 
  1944 г. На советско-германском фронте она &quot;засветилась&quot; только один 
  раз, причём воевала плохо. На Западе дивизия воевала лучше, но в этом была заслуга 
  скорее не Власова, а его противников. Когда американцы предложили власовцам 
  сдаться, в качестве награды они сдуру пообещали... беспрепятственное возвращение 
  на Родину. Стоит ли говорить, что после этого оборона 1-й дивизии РОА стала 
  и вовсе неприступной! Сам Власов сдаться союзникам хотел, но не смог - они его 
  просто не приняли. Зато приняла советская военная контрразведка Смерш. В итоге 
  был суд, Власова и 11 других предателей повесили в Лефортовской тюрьме 2 августа 
  1946 г. <br>
  <br>
  ...А ведь пойди наступление под Новгородом чуть успешнее, всё сложилось бы по-другому. 
  Власов не сдался бы в плен и, скорее всего, довоевал бы до конца войны не хуже: 
  других генералов. Он дошёл бы до Берлина, стал маршалом и Героем Советского 
  Союза, закончил свои дни в почёте и славе. В уходящем сентябре мы бы как раз 
  отмечали 105-летие со дня его рождения. <br>
