  Слова Николая Флерова<br>
  <br>
  От волжских круч до крепостей Кронштадта,<br>
  От южных скал до северной земли<br>
  Мы в бескозырках и в морских бушлатах<br>
  Дорогой легендарною прошли.<br>
  И с той поры тех памятных походов<br>
  Я в море дальнем и на берегу,<br>
  Как боевую славу мореходов,<br>
  Тебя, бушлат любимый, берегу.<br>
  <br>
  <em>Припев:</em><br>
  <br>
  Поговорим с тобой наедине…<br>
  Ты был со мной в бою – в дыму, в огне.<br>
  Ты видел Севастополь,<br>
  Ты видел Ленинград,<br>
  Мой славный морской бушлат.<br>
  <br>
  Я помню час: декабрьской пургою<br>
  В бой за Москву шагал морской отряд.<br>
  Я ранен был. Тогда меня тобою<br>
  Друзья укрыли, дорогой бушлат.<br>
  И сразу мне теплей и легче стало,<br>
  Я сквозь дремоту слышал пушек гром.<br>
  И нам с тобою сердце подсказало,<br>
  Что все вперед на запад мы идем.<br>
  <br>
  Тот день настал: военные метели<br>
  Утихли над волной и над землей.<br>
  Во фронтовых бушлатах и шинелях<br>
  Мы шли на праздник дружною семьей.<br>
  Дошел и я до гавани далекой,<br>
  Где долго ждали и жена и мать.<br>
  И лишь тогда подруге кареокой<br>
  С усталых плеч тебя позволил снять.<br>
  <br>
  Шумят хлеба, в печах металл вскипает,<br>
  Гудит в плотинах волжская вода,<br>
  К высотам счастья радостно шагает<br>
  Отчизна славы, мира и труда.<br>
  Но если вновь, безумием объятый,<br>
  Решится враг на нас пойти войной,<br>
  То выйдем мы в шинелях и бушлатах,<br>
  Чтоб дать ему последний грозный бой.<br>
  <br>
  <em>Русские советские песни (1917-1977). Сост. Н. Крюков и Я. Шведов. 
