	<span class="header"><strong>ПЕСНЯ О БУШЛАТЕ </strong><br></span> 
  <br>
  Музыка Бориса Терентьева<br>
  Слова Николая Флёрова<br>
  <br>
  От волжских круч, от города Кронштадта, <br>
  От южных скал и северной земли <br>
  Мы в бескозырках и в морских бушлатах <br>
  Дорогой боя грозного прошли. <br>
  И с той поры, с тех памятных походов <br>
  Я в море дальнем и на берегу, <br>
  Как боевую славу мореходов, <br>
  Тебя, бушлат любимый, берегу. <br>
  <br>
  <em>Припев:</em><br>
  <br>
  Поговорим с тобой наедине... <br>
  Ты был со мной в бою - в дыму, в огне, <br>
  Ты видел Севастополь, <br>
  Ты знаешь Ленинград, <br>
  Мой славный морской бушлат. <br>
  <br>
  Я помню день: декабрьскою пургою <br>
  В бой за Москву шагал морской отряд. <br>
  Я ранен был, и в этот час тобою <br>
  Меня укрыли, дорогой бушлат. <br>
  И сразу мне теплей и легче стало, <br>
  Я сквозь дремоту слышал пушек гром, <br>
  И нам с тобою сердце подсказало, <br>
  Что все вперед, на запад мы идем. <br>
  <br>
  Настанет час - военные метели <br>
  Утихнут над волной и над землей.<br>
  Почистим мы бушлаты и шинели, <br>
  Придем на праздник дружною семьей. <br>
  Дойду и я до гавани далекой,<br>
  Где ждет жена и ждет старушка-мать,<br>
  И лишь тогда подруге кареокой<br>
  С усталых плеч тебя позволю снять.<br>
  <br>
  1941<br>
  <br>
  <em>Священная война. Песни Победы / Сост. В. А. Костров, Г. Н. Красников 
  - М.: Олимп, 2005.</em><br>
<br>
  <br>
  Одна из первых песен, записанных на пластинку после возобновления в 1942 году 
  работы московского Дома звукозаписи и Апрелевского завода грампластинок (они 
  не работали с октября 1941-го). Запись началась с коллективов, входящих в Центральный 
  объединенный ансамбль Военно-Морского флота СССР п/у Вано Мурадели, поэтому 
  много песен оказалось на флотскую тематику. Ансамбль записывался с 31 августа 
  и в течение сентября 1942. Были записаны &quot;Месть балтийцев&quot; и &quot;Песня 
  морской пехоты&quot; М. Юдина и Н. Браун, <a href="igrbajan.php">&quot;Играй, 
  мой баян&quot;</a> (&quot;Застава дорогая&quot;) В. Соловьева-Седого, Л. Давидович 
  и Г. Ромма, <a href="beskozyrka.php">&quot;Бескозырка&quot;</a> И. Жака и Н. 
  Верховского, <a href="buchlat.php">&quot;Бушлат&quot;</a> Б. Терентьева и Н. 
  Флерова и др.<br>
  <br>
  После победы Флеров подправил текст (в третьем куплете исправил 
  будущее время на прошлое и добавил четвертый куплет), чтобы песня выглядела соответственно 
  времени.<br>
  <br>
