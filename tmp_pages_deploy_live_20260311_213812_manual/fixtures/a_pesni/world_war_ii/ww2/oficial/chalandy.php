  <em>Шел трамвай десятый номер…Городские песни. Для голоса в сопровождении 
  фортепиано (гитары). / Сост. А. П. Павлинов и Т. П. Орлова. СПб., &quot;Композитор 
  – Санкт-Петербург&quot;, 2005.</em><br>
<br>
  <br>
  Песня написана для кинофильма Леонида Лукова &quot;Два бойца&quot; (1943), где 
  ее исполнил Марк Бернес, игравший бойца-одессита Аркадия Дзюбина.<br>
  <br>
  Песня не предполагалась сценарием: Бернес должен был спеть только одну песню 
  - <a href="temnnotch.htm">&quot;Темная ночь&quot;</a>. Но о &quot;Темной ночи&quot; 
  прослышал Утесов и включил в свой репертуар, не дожидаясь выхода фильма на экран. 
  Потому срочно нужна была еще одна песня, не известная зрителям. Было дано объявление 
  в газете, приглашающее всех, кто знает одесские песни, прийти в назначенное 
  время на Мосфильм для прослушивания. Пришло огромное количество народу - от 
  профессоров до уркаганов. На основе услышанного петербуржец Богословский написал 
  музыку. Киевлянин Владимир Агатов сочинил к ней &quot;одесский текст&quot;.<br>
  <br>
  Песня с выходом фильма приобрела огромную популярность. При этом, она не рекомендовалась 
  для официального исполнения и не публиковалась, так как считалась блатной. Сам 
  Богословский дразнил ее &quot;баланда, полная фекалий&quot;. Агатов вскоре 
  угодил в сталинские лагеря, откуда вышел только во времена &quot;оттепели&quot;. 
  Подробнее о песне см. Михаил Пойзнер <a href="../../dvor/odessa/a-ihneznali.php">&quot;Их 
  не знали только в лицо...&quot;</a> (2008).<br>
