	<br>
  <br>На уроках литературы в школе мы часто говорим о плодотворном взаимовлиянии 
    фольклора и литературы. Но, как правило, понимаем это односторонне: выясняем, 
    как использовал писатель в своем творчестве пословицы и поговорки, строки 
    из лирических или исторических песен, былинные или сказочные образы, постоянные 
    эпитеты или троекратные повторы, психологический параллелизм или ступенчатое 
    сужение... и так далее, и тому подобное. Но очень редко говорим о влиянии 
    литературы на устное поэтическое творчество. В лучшем случае вспоминаем, что 
    значительную часть русского народного песенного репертуара составляют положенные 
    на музыку стихотворения Пушкина, Дельвига, Мерзлякова, Кольцова, Никитина, 
    Сурикова, Некрасова и многих других поэтов, имена которых известны порою лишь 
    специалистам. И уж совсем не касаемся мы на уроках истории жизни литературных 
    произведений в их фольклорном бытовании. А ведь многие из них стали импульсом 
    к появлению целых циклов фольклорных песен. Так, в годы Великой Отечественной 
    войны было создано немало устных и рукописных вариантов &quot;Катюши&quot; 
    Михаила Исаковского, &quot;Землянки&quot; Алексея Суркова, &quot;Синего платочка&quot; 
    и других популярных песен. Думаю, что знакомство с предлагаемыми ниже заметками 
    поможет учителю в подготовке к урокам по поэзии военных лет. Ведь фронтовые 
    стихотворные отклики на песни поэтов — это и свидетельство того, что поэты 
    сумели взволновать души многих миллионов читателей и слушателей, и того, что 
    поэзия была необходима людям даже в страшном пекле войны. Так было на самом 
    деле, это не романтическая выдумка наших дней. Подтверждают это самые объективные 
    свидетели—фронтовики. В день сорок пятой годовщины Победы у меня была счастливая 
    возможность беседовать с поэтом-фронтовиком Михаилом Александровичем Дудиным. 
    Не удержался, спросил: &quot;Не преувеличивают ли фольклористы значение песни 
    на фронте?&quot; Поэт решительно развеял мои сомнения. Да, песня звучала и 
    во фронтовой землянке, и в госпитальной палате, и в лесном лагере партизан. 
    &quot;Правда, — посетовал поэт, — фольклористы далеко не все записывали и 
    уж тем более не все публиковали, Ведь часто в солдатских песнях было и слишком 
    острое слово, и слишком дерзкое&quot;. Сам поэт помнил такие песни и охотно 
    их цитировал. <br>
  <br>Ну а самыми популярными в годы войны были песни о Катюше. <br>
  <br>Имя Михаила Васильевича Исаковского (1900-1973) широко известно в нашей стране. 
    Но его безымянная слава несравненно больше его известности. Ведь миллионы 
    людей пели, не вспоминая имени автора: &quot;Дан приказ ему на запад...&quot;, 
    &quot;В лесу прифронтовом...&quot;, &quot;Враги сожгли родную хату...&quot;. 
    Они искренне грустили, напевая &quot;старинный вальс &quot;Осенний сон&quot;, 
    смахивали слезу вместе с солдатом, который &quot;нашел ... в широком поле 
    травой заросший бугорок&quot;. <br>
  <br>Исаковский сложил свои песни из удивительно простых слов, которыми сумел 
    передать и радость, и горе своего народа, и слова эти стали поистине народными 
    песнями. Среди них особое место принадлежит &quot;Катюше&quot;. Вот уже полвека 
    поет ее страна. Да и не только наша. &quot;Катюшу поют в других странах, на 
    других материках. Более того. Когда на одном из международных фольклорных 
    фестивалей в Загребе запели &quot;Катюшу&quot;, югославы совершенно серьезно 
    стали утверждать, что это их песня и пелась она якобы в Сербии и Хорватии 
    еще во времена прошлой войны... <br>
  <br>Мне самому довелось быть свидетелем подобной ситуации. Несколько лет назад 
    наша студенческая группа ехала в поезде из Варшавы в древнюю столицу Польши 
    — Краков. Настроение у всех было отличное. Расчехлили гитару, и по вагону 
    полилась песня. Пассажиры переполненного вагона слушали нас с интересом. А 
    когда мы запели &quot;Катюшу&quot;, один мужчина, уже пожилой, растрогался 
    до слез: &quot;Ладно, бардзо ладно. — И прибавил — Это хорошая польская песня&quot;. 
  <br>
  <br>Вот какую популярность завоевала девушка, пославшая свой привет &quot;бойцу 
    на дальнем пограничье&quot;. <br>
  <br>Стихотворение &quot;Катюша&quot; было написано в 1938 году. А песней оно 
    стало в следующем — тридцать девятом. Ее появление именно в то время было 
    не случайным. Поэзия тех лет остро переживала состояние приближающейся военной 
    грозы. Николай Тихонов пишет тогда свои знаменитые строки: <br>
  <br>
  <em>Я хочу, чтоб в это лето, <br>
  Лето, полное угроз, <br>
  Синь военного берета <br>
  Не коснулась ваших кос. </em><br>
  <br>От страшной судьбы растоптанной фашистским сапогом Европы не может отвести 
    потрясенного взгляда Павел Антокольский (стихотворение &quot;Новогодняя хроника&quot;, 
    &quot;Последние известия&quot;). <br>
  <br>Сгущаются тучи и над нашими западными рубежами. Становится ясно, что, защищая 
    родную землю, вот-вот примет на себя первый удар воин в зеленой фуражке. На 
    него смотрят с любовью и надеждой, ему посвящаются стихи и песни. О защитниках 
    передних рубежей пишет в эти годы несколько стихотворений и М.В. Исаковский: 
    &quot;Шел со службы пограничник&quot;, &quot;В родном краю&quot;, &quot;У 
    самой границы&quot;. Но особенно популярной стала переложенная композитором 
    М. Блантером на музыку &quot;Катюша&quot;. Почему? Да, наверное, потому, что 
    в ней оказались счастливо сплавлены лучшие песенные качества: музыкальность 
    стиха и простота сюжета, близкого и понятного многим — обращение девушки к 
    возлюбленному, полное заботы о нем. Казалось бы, старая-престарая сюжетная 
    ситуация, гениально воспроизведенная еще в &quot;Слове о полку Игореве&quot;. 
    Помните, Ярославна на стене древнего Путивля обращается к Солнцу и Ветру с 
    просьбой помочь Игорю? Но эта тема и этот сюжет — на все времена. Исаковский 
    повторил его, но сделал это так, что стихи стали &quot;своими&quot;, сокровенными 
    для миллионов людей. И вот это восприятие &quot;Катюши&quot; народом, даже 
    народами, как чего-то своего, личного, задушевного стало причиной удивительного 
    явления — рождения множества новых песен-переложений. На привет-послание девушки 
    бойцу-пограничнику последовали песенные же ответы с пограничных застав. В 
    них воины обращались к подругам, реальным или воображаемым, называя их одним 
    ласковым именем: <br>
  <br>
  <em>Не цветут здесь яблони и груши, <br>
  Здесь леса прекрасные растут. <br>
  Каждый кустик здесь бойцу послушен,<br>
  И враги границу не пройдут. <br>
  Не забыл тебя я, дорогая, <br>
  Помню, слышу песенку твою <br>
  И в дали безоблачного края <br>
  Я родную землю берегу. <br>
  Не забудь и про меня, Катюша, <br>
  Про того, кто письма часто шлет, <br>
  Про того, кто лес умеет слушать, <br>
  Про того, кто счастье бережет. </em><br>
  <br>Но это было все же только началом военной биографии &quot;Катюши&quot;. Настоящим 
    бойцом она стала в годы Великой Отечественной. Солдаты-фронтовики сочинили 
    большое количество песен о любимой героине. В одной из них девушка оказывается 
    на захваченной врагом территории, ее угоняют в рабскую неволю в Германию: 
  <br>
  <br>
  <em>Здесь звенела песенка Катюши,<br>
  А теперь никто уж не поет:<br>
  Сожжены все яблони и груши,<br>
  И никто на берег не придет...</em><br>
