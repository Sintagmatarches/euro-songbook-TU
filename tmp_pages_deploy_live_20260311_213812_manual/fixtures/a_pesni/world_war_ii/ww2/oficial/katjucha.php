    неволю. Свод этих песен по широте и глубине показа человека на войне может 
    сравниться разве что с &quot;Василием Теркиным&quot; Александра Твардовского. 
    Причем важно то, что главное в этой поэтической энциклопедии — показ войны 
    &quot;изнутри&quot;. Через сокровенные переживания, доверяемые чаще всего 
    письму, адресованному близкому человеку. Отсюда тот пронзительный лиризм, 
    что и сегодня трогает человеческое сердце. <br>
