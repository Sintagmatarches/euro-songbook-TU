  Лазить в наш советский огород. <br>
  И летят снаряды в тьму густую, <br>
  И огнем окрашен небосвод. <br>
  Пусть услышат &quot;девушку простую&quot;,<br>
  Пусть запомнят, как она поет!</em><br>
    <br>
    Если собрать все песни о &quot;Катюше&quot;, созданные за время Великой Отечественной, 
