 <br>
  <br>
  Песня была предназначена для дебютного концерта Госджаза СССР п/у Виктора Кнушевицкого 
  и Матвея Блантера 21 ноября 1938 года в Колонном зале Дома Союзов. Там ее исполнила 
  Валентина Батищева (джазовая певица, в 1930-е годы она выступала с джаз-оркестрами 
  в фойе кинотеатров и на эстраде крупнейшего тогда столичного ресторана &quot;Москва&quot;). 
  Песня трижды вызывалась на &quot;бис&quot;. Но первое исполнение произошло еще 
  раньше, случайно: на последней репетиции Госджаза присутствовала исполнительница 
  народных песен Лидия Русланова. Она не удержалась, и через несколько часов спела 
  песню по памяти на концерте в том же самом Колонном зале:<br>
  <br>
  <em>&quot;- Как же так? - спросили Лидию Андреевну музыканты. - Ведь у вас не 
  было даже нот?!<br>
  - А зачем ноты? Я запомнила песню мгновенно! - ответила певица.<br>
  - Но ведь вы поете русские песни, а это фокстрот, - продолжали недоумевать музыканты.<br>
  - Это прекрасная русская песня, - сказала Лидия Андреевна. - И простите меня, 
  она так понравилась мне, что я не могла удержаться и не спеть ее - песня требовала 
  немедленного выхода!&quot;</em> (Из книги Глеба Скороходова &quot;Тайны граммофона&quot;. 
  М.: Изд-во Эксмо, Изд-во Алгоритм, 2004, с. 71.)<br>
  <br>
  <br>
  Фраза &quot;на дальнем пограничье&quot; вызывает ложные ассоциации с Дальним 
  Востоком - в 1937 году на маньчжурской границе произошло первое боевое столкновение 
  между СССР и Японией. Но вообще песня о западной границе СССР 
  - с Польшей. Песенка девушки летит &quot;за ясным солнцем вслед&quot; - то есть 
  с Востока на Запад. Именно с Запада ждали большой войны. Эта предвоенная песня 
  стала одной из любимых песен Великой Отечественной войны и послужила основой 
  для огромного числа переделок. Лидия Русланова стала наиболее известной ее исполнительницей. 
  Подробнее см.: И. Н. Розанов. Песня о Катюше как новый тип народного творчества. 
  / Русский фольклор Великой Отечественной войны. Изд. «Наука», М.—Л., 1964, стр. 
  310—325. <br>
  <br>
