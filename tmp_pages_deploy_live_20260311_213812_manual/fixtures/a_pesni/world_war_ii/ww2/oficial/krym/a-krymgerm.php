	 <strong>Олег Романько,<br>
  кандидат исторических наук, доцент кафедры украиноведения Крымского государственного 
  медицинского университета им. С.И. Георгиевского (г. Симферополь)</strong><br>
  <br>
<span class="header"><strong><em>К 60-летию освобождения Крыма</em><br>
<br>
  КРЫМСКОТАТАРСКИЕ ДОБРОВОЛЬЧЕСКИЕ ФОРМИРОВАНИЯ В ГЕРМАНСКИХ ВООРУЖЕННЫХ СИЛАХ 
  (1941—1945) </strong><br></span>
  <br>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=86&par=74&l=&art=293" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, №6-7, 
  2004</em></a><em>.</em><br>
  <br>
  <br>
  Проблема использования граждан СССР в составе германских вооруженных сил является 
  на сегодняшний день одной из наиболее сложных и запутанных в истории Второй 
  мировой войны. Только в последние годы у отечественных историков появилась возможность 
  вплотную подойти к этой проблеме, о чем свидетельствуют многочисленные публикации 
  [1]. Оказалось, что она неоднозначна и содержит в себе многочисленные аспекты.<br>
  <br>
  Так, наряду с морально-нравственной стороной такого явления, как коллаборационизм, 
  его политической сущностью, одним из аспектов, привлекающих внимание историков, 
  является национальная принадлежность наших соотечественников, служивших в вермахте 
  и в войсках СС в составе так называемых добровольческих формирований.<br>
  <br>
  Кроме того, в современных социально-политических условиях — пренебрежительного 
  отношения к историческому прошлому нашей Родины и спекуляций им — этот вопрос 
  приобрел неожиданную актуальность.<br>
  <br>
  Другой стороной этой проблемы является недостаточная изученность такого вопроса, 
  как национальная политика Германии на оккупированных территориях СССР в период 
  Великой Отечественной войны. «Отношение германского правительства… к другим 
  народам… имеет очень большое значение, — писал историк из ФРГ К.Г. Пфеффер. 
  — Весьма распространенное убеждение, что немцы относились к другим народам, 
  как правило, отрицательно, является неверным. В действительности… отношение 
  было сильно дифференцированным» [2].<br>
  <br>
  С этим утверждением приходится согласиться, так как именно использованию «национального 
  вопроса» в германской оккупационной политике и пропаганде нацисты отводили очень 
  важную роль. Особенную актуальность он приобрел перед подготовкой и в ходе осуществления 
  плана «Барбаросса». Готовя войну против Советского Союза, руководство Германии 
  рассматривало его как «искусственное и рыхлое объединение» огромного числа наций, 
  как «этнический конгломерат», лишенный внутреннего единства [3]. Поэтому одной 
  из главных задач германского военно-политического руководства после начала войны 
  с СССР было разрушение его как многонационального государства путем привлечения 
  на свою сторону представителей нерусских народов и национальных меньшинств. 
  При этом нацисты считали, что «для борьбы с большевизмом стало возможным привлечь 
  на свою сторону многочисленные мусульманские народы Советского Союза», на сотрудничество 
  с которыми делалась особая ставка [4]. Одним из способов осуществления такого 
  сотрудничества стало создание «национальных добровольческих формирований» из 
  представителей тюркских и кавказских народов СССР.<br>
  <br>
  Первым оккупированным регионом Советского Союза, где немцы на практике применили 
  политику использования нерусских мусульманских народов в военных целях, был 
  Крым с его пестрым национальным составом населения. Именно здесь в октябре—ноябре 
  1941 г. немцы сформировали первые «мусульманские легионы» из крымских татар.<br>
  <br>
  Процесс создания крымскотатарских добровольческих формирований имел в своей 
  основе те же политические и военные причины, что обусловили создание подобных 
  формирований из других мусульманских народов СССР. Однако имелись и свои отличительные 
  черты, наложившие на него свой, «местный», отпечаток.<br>
  <br>
  В ноябре 1941 г. Крым под названием «Генеральный округ «Таврия» стал составной 
  частью одной из новых административных единиц, созданных немцами на оккупированной 
  советской территории, — рейхскомиссариата «Украина», — власть в котором должна 
  была осуществляться гражданской администрацией [5]. Однако вследствие того, 
  что Крым, практически до своего освобождения в 1944 г., являлся либо тыловым 
  районом действующих армий, либо зоной боевых действий, фактическая власть принадлежала 
  командующему расквартированных здесь частей вермахта. Эта власть еще более усилилась, 
  когда в 1942—1943 гг. Крым стал тыловым районом немецких войск, наступающих 
  на Кавказ.<br>
  <br>
  С конца 1941 г. важным фактором, оказавшим влияние на немецкую оккупационную 
  политику (в том числе и в Крыму), стало партизанское движение. Один из немецких 
  офицеров позднее писал: «[Партизанское движение] не было, конечно, просто проявлением 
  беспорядка в тыловых областях, как сперва думали немцы. Напротив, это было политическое 
  движение сопротивления, которое невозможно было взять под контроль лишь силами 
  полиции» [6]. В Крыму эта проблема приобрела для немецкого командования особенную 
  актуальность, так как районы сосредоточения и операций советских партизан находились 
  в непосредственной близости от населенных пунктов и коммуникаций, важных с оперативной 
  точки зрения.<br>
  <br>
  Таким образом, главной задачей немецких оккупационных властей в Крыму стало 
  использование его с военной точки зрения как тылового района действующих армий. 
  Для этого следовало добиться лояльности населения и уничтожения партизанского 
  движения, чему должны были быть подчинены все проводимые здесь оккупационными 
  властями мероприятия, включая попытки привлечения на свою сторону в целях сотрудничества 
  местного населения. Поэтому процесс использования добровольческих частей приобрел 
  форму создания «местных полицейских вспомогательных сил» для поддержания общественного 
  порядка и, в меньшей степени, для привлечения добровольцев с целью включения 
  их в части действующей в Крыму германской армии в качестве «добровольных помощников» 
  — «хиви» (сокращенный немецкий термин «Hilfswillige»).<br>
  <br>
  Главным органом по обеспечению порядка на территории Крыма была печально известная 
  айнзатцгруппа «Д», во главе которой стоял СС-оберфюрер О. Олендорф*.<br>
  <br>
  <hr>
  <em>* Олендорф, Отто, род. 4.02.1907 г., один из руководителей карательной системы 
  Германии, СС-группенфюрер (1944). В 1925 г. вступил в НСДАП и СА, в 1926 г. 
  перешел в СС. 27.09.1936 г., когда начало формироваться РСХА, возглавил его 
  3-е управление, ведавшее службой безопасности внутри Германии (СД). С июня 1941 
  по июль 1942 г. — начальник айнзатцгруппы «Д». В мае 1945 г. арестован союзниками 
  и был признан виновным в уничтожении 91 тысячи человек. 10.04.1948 г. приговорен 
  к смертной казни через повешение. 8.06.1951 г. приговор приведен в исполнение. 
  </em><br>
  <hr>
  <br>
  Созданная по инициативе Главного управления имперской безопасности (РСХА), она 
  являлась «инструментом проведения расовой политики» на «восточных» территориях 
  и занималась уничтожением «евреев, коммунистов и прочих «нежелательных элементов». 
  В течение лета—осени 1941 г. эта группа уничтожила на юге Украины и в Крыму 
  до 40 тысяч человек [7]. Две трети ее персонала (около 400 человек) состояло 
  из «местных добровольцев»: русских, украинцев и татар.<br>
  <br>
  Кроме «основных» функций на группу было возложено создание отделений полиции 
  порядка (Ordnungspolizei) на оккупированной территории в зоне ее действия. Именно 
  ее сотрудниками были созданы городские и районные отделения полиции порядка 
  Крыма.<br>
  <br>
  В ноябре 1941 г. все «местные полицейские вспомогательные силы» на территории 
  рейхскомиссариатов были организованы в подразделения «вспомогательной полиции 
  порядка» (Schutzmannschaft der Ordnungspolizei или “Schuma”). Собственно полиция 
  «Schuma» состояла из следующих категорий:<br>
  <br>
  - полиция порядка в городах и сельской местности — Schutzmannschaft-Einseldienst;<br>
  - отряды самообороны — Selbst-Schutz;<br>
  - полицейские батальоны для борьбы с партизанами — Schutzmannschaft-Bataillone;<br>
  - вспомогательная пожарная полиция — Feuerschutzmannschaft;<br>
  - резервная вспомогательная полиция для охраны лагерей военнопленных и несения 
  трудовой повинности – Hilfsschutzmannschaft [8].<br>
  <br>
  Отделения городской и сельской полиции создавались сразу же после занятия немцами 
  городов и крупных населенных пунктов Крыма. Обычно ею руководил начальник отдела 
  вспомогательной полиции порядка при городском или районном управлении, который, 
  однако, подчинялся местному фюреру СС и полиции. Главными обязанностями ее сотрудников 
  были поддержание порядка в населенном пункте и наблюдение за исполнением паспортного 
  режима [9].<br>
  <br>
  Личный состав полиции состоял в основном из трех национальных групп: татар, 
  украинцев и русских. Причем национальный состав варьировался в зависимости от 
  района. Так, татары преобладали в полиции Алушты (начальник — Чермен Сеит Мемет), 
  Ялты, Севастополя (начальник — Ягъя Алиев), Карасубазара и Зуи (начальник — 
  старший полицейский Алиев), значительно меньше их было в полиции Евпатории и 
  Феодосии [10].<br>
  <br>
  Однако ни городская, ни сельская полиция не могли самостоятельно бороться с 
  партизанами, а тем более уничтожить их. Поэтому оккупационные власти делали 
  все, чтобы создать более крупные вооруженные формирования, которые могли бы 
  обеспечить относительный порядок, хотя бы в пределах своего района.<br>
  <br>
  2 декабря 1941 г. Верховное командование сухопутных войск (ОКХ) издало директиву 
  «Особые указания для борьбы с партизанами». В ней, в частности, говорилось: 
  «…Использование местных отрядов в борьбе с партизанами вполне себя оправдывает. 
  Знание местности, климата и языка страны делает возможным в боях с партизанами 
  применить их же методы действий» [11].<br>
  <br>
  Одним из принципов немецкой оккупационной политики на территории СССР вообще 
  и в создании добровольческих формирований в частности было противопоставление 
  нерусских народов и национальных меньшинств народу русскому. В Крыму этот принцип 
  нашел свое отражение в заигрывании немецких властей с крымскотатарским населением 
  и в создании из его представителей добровольческих формирований в виде отрядов 
  самообороны и батальонов «Schuma» для использования на территории полуострова.<br>
  <br>
  По словам генерал-полковника Э. фон Манштейна, командующего 11-й немецкой армией, 
  которая в октябре—ноябре 1941 г. оккупировала Крым, «татары сразу же встали 
  на нашу (немцев) сторону. Они видели в нас своих освободителей от большевистского 
  ига, тем более, что мы уважали их религиозные обычаи…» [12].<br>
  <br>
  Кроме того, еще в октябре 1941 г. появились первые свидетельства того, что крымские 
  татары начали дезертировать из действующих в Крыму советских войск, скрываясь 
  в своих деревнях. По словам очевидцев, «мало того, что татары дезертировали 
  сами, но они, под видом дружбы, развращали и русских бойцов, убеждая их покидать 
  позиции и обещая скрывать их в своих деревнях» [13].<br>
  <br>
  В ряде случаев имело место открытое нападение татар на отступающие советские 
  части, а также разграбление партизанских продовольственных баз, созданных перед 
  войной [14]. Так, например, 18 декабря 1941 г. разведка Феодосийского партизанского 
  отряда обнаружила в лесу 40 подвод с вооруженными татарами, которые, как выяснилось, 
  приехали за продовольствием отряда. Этой группой руководил дезертир из Судакского 
  партизанского отряда бывший лейтенант Красной Армии и член ВКП(б) Меметов [15]. 
  Грабежом партизанских продовольственных баз также занимались жители татарских 
  деревень Баксан, Тау-Кипчак, Мечеть-Эли, Вейрат, Конрат, Еуртлук, Ени-Сала, 
  Молбай, Камышлык, Аргин, Ени-Сарай, Улу-Узень, Казанлы, Корбек, Коуш, Биюк-Узенбаш, 
  Кучук-Узенбаш, Ускут.<br>
  <br>
  После оккупации большей части Крыма немцы начали проводить открытую политику 
  заигрывания с татарским населением, используя националистические настроения 
  и создавая для татар ряд материальных преимуществ перед остальными народами 
  Крыма. Была проведена организация специальных магазинов, передача татарам лучших 
  домов, приусадебных участков и колхозного инвентаря. Например, в Симферополе 
  патенты на торговлю выдавались прежде всего татарам, затем грекам и армянам, 
  а уже в последнюю очередь караимам и русским.<br>
  <br>
  Немецкие оккупационные власти во многих случаях не подвергали репрессиям комсомольцев 
  и коммунистов-татар, а разъясняли им, что «они раньше ошибались, а теперь с 
  оружием в руках должны загладить свои ошибки» [16].<br>
  <br>
  «Уже в октябре 1941 г., — пишут английские исследователи Ч. Диксон и О. Гейлбрунн, 
  — для борьбы с партизанами немцы стали привлекать также (крымских) татар, которые 
  всегда враждебно относились к большевистскому режиму. Были сформированы так 
  называемые «татарские отряды самообороны», которые оказали немцам большую помощь» 
  [17]. Этим отрядам, насчитывавшим по 70—100 человек, выдавалось трофейное стрелковое 
  оружие, назначались инструктора — немецкие унтер-офицеры. По словам Э. фон Манштейна, 
  главная задача этих отрядов «заключалась в охране своих селений от нападения… 
  партизан» [18]. Создание подобных отрядов облегчалось наличием в деревнях значительного 
  количества мужчин призывного возраста, которых не успели призвать в ряды Красной 
  Армии.<br>
  <br>
  В ноябре 1941 г., одним из первых, отряд самообороны был создан в деревне Коуш. 
  Его командиром был назначен местный житель, некто Раимов, дослужившийся в немецкой 
  полиции до чина майора. Активное участие в создании отряда принял староста деревни 
  О. Хасанов — в недавнем прошлом член ВКП(б). Главной задачей этого отряда было 
  «частыми нападениями и диверсиями держать в постоянном напряжении партизан, 
  истреблять их живую силу, грабить продовольственные базы» [19]. На тот момент 
  в отряде проходили службу 80 человек. Помимо этого Коуш был центром вербовки 
  добровольцев-татар в данном районе [20]. Благодаря трем линиям сильных укреплений 
  Коуш долгое время был неуязвим для партизан [21].<br>
  <br>
  В результате немецкой кампании по организации отрядов самообороны к декабрю 
  1941 г. они были сформированы уже в следующих населенных пунктах: Ускут (130 
  чел.), Туак (100 чел.), Кучук-Узень (80 чел.), Ени-Сала, Султан-Сарай, Баши, 
  Карасу-Баши, Молбай и в ряде других [22].<br>
  <br>
  До января 1942 г. создание отрядов самообороны носило неорганизованный характер 
  и зависело от инициативы местных немецких военных комендантов. После поражения 
  немцев под Москвой, Ростовом, а также после начала Керченско-Феодосийской десантной 
  операции ситуация коренным образом изменилась.<br>
  <br>
  2 января 1942 г. в отделе разведки 11-й немецкой армии состоялось совещание, 
  в ходе которого было заявлено, что Гитлер разрешил призыв добровольцев из числа 
  крымских татар. Штаб армии передал решение этого вопроса руководству айнзатцгруппы 
  «Д». Перед ним ставились следующие задачи: «Охватить крымских татар, способных 
  служить в армии, для действий на фронте в частях 11-й армии на добровольной 
  основе, а также создать татарские роты самообороны, которые совместно с айнзатцгруппой 
  «Д» будут использованы для борьбы с партизанами» [23].<br>
  <br>
  3 января 1942 г. под руководством О.Олендорфа началось заседание созданного 
  23 ноября 1941 г. Симферопольского мусульманского комитета. Оно было посвящено 
  решению вопроса о начале вербовки крымских татар в германские вооруженные силы 
  для общей «борьбы против большевизма» [24]. На комитет и его председателя Джамиля 
  Абдурешидова* были возложены обязанности по пропагандистской подготовке вербовочной 
  кампании в отдельных населенных пунктах. На руководство айнзатцгруппы «Д» возлагалась 
  техническая сторона вопроса. Помимо этого оно должно было сотрудничать с отделами 
  комитета, которые непосредственно отвечали за работу с завербованными добровольцами: 
  отделом по борьбе с бандитами (то есть советскими партизанами) (руководитель 
  — А. Абдулаев*) и отделом по вербовке и организации добровольческих отрядов 
  (руководитель — Т. Джемилев*) [25].<br>
  <br>
  5 января 1942 г. с формального согласия председателя мусульманского комитета 
  в Симферополе было открыто вербовочное бюро и начался набор добровольцев под 
  лозунгом: «Татары, хотите, чтобы вас не грабили партизаны, берите добровольно 
  оружие против партизан» [26]. Для осуществления вербовки добровольцев на местах 
  от комитета были посланы специальные уполномоченные — вербовщики: Б. Аджиев*, 
  Ш. Карабаш* и А. Карабаш*. Главным уполномоченным комитета по проведению вербовочной 
  компании был назначен Г. Аппаз* [27].<br>
