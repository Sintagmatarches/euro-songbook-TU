  <hr>
  <br>
  «Azat Kirim» печатала материалы об организации в районах Крыма мусульманских 
  комитетов, их работе по обеспечению населения, перераспределению земельной собственности, 
  религиозно-культурному воздействию на крымских татар; о вербовке и службе татарских 
  добровольцев в германских вооруженных силах; сводки с театров боевых действий; 
  материалы об открытии мечетей; здравицы в честь «освободительной» германской 
  армии и «освободителя угнетенных народов, верного сына германского народа Адольфа 
  Гитлера» [37]. Помимо этого, в 1943 г. редакция газеты сообщила своим подписчикам, 
  что «в иностранной информации большое внимание будет уделяться событиям, происходящим 
  на Ближнем Востоке и в Индии» [38].<br>
  <br>
  В организации и подготовке крымскотатарских отрядов самообороны существенная 
  роль отводилась религиозному воспитанию добровольцев, которое осуществлялось 
  путем тесного сотрудничества германских оккупационных властей и мусульманского 
  духовенства. Следует сказать, что многие представители последнего одобряли набор 
  крымских татар в германские вооруженные силы. Так, на упоминавшемся заседании 
  Симферопольского мусульманского комитета 3 января 1942 г. присутствовал главный 
  мулла городского мусульманского объединения. В конце заседания он взял слово 
  и заявил, что «его религия и верования требуют принять участие в священной борьбе 
  совместно с немцами, ибо окончательная победа для них (татар. — О.Р.) не только 
  означает уничтожение советского господства, но снова дает возможность следовать 
  их религиозным и моральным обычаям» [39].<br>
  <br>
  В январе 1942 г., после начала кампании по набору добровольцев, многие муллы 
  работали членами вербовочных комиссий. Их главной задачей была подготовка общественного 
  мнения с целью привлечения наибольшего количества добровольцев [40].<br>
  <br>
  Однако помимо «духовной пищи» вступившим в такие формирования обещалось хорошее 
  материальное обеспечение и создание всяческих льгот и привилегий для их семей. 
  Так, согласно постановлению Верховного командования вермахта (ОКВ), «всякое 
  лицо, которое активно боролось или борется с партизанами и большевиками», могло 
  подать прошение о «наделении его землей или выплате ему вознаграждения до 1000 
  рублей…» [41]. После опубликования 15 февраля 1942 г. «Закона о новом аграрном 
  порядке», подготовленного министром оккупированных восточных областей А. Розенбергом, 
  всем татарам, вступившим в добровольческие формирования, и их семьям стали давать 
  по 2 га земли в полную собственность. Им предоставляли лучшие участки, не считаясь 
  с тем, что они принадлежали крестьянам, не вступившим в отряды самообороны [42].<br>
  <br>
  Главной задачей рот самообороны была совместная с немецкими оккупационными войсками 
  борьба против партизан. Для этого использовались наиболее обученные и подготовленные 
  роты (например, 8-я (Бахчисарай) и 9-я (Коуш)); те же роты, которые еще не прошли 
  достаточной подготовки, применялись для несения караульной службы на военных 
  и гражданских объектах: складах, железнодорожных станциях, в административных 
  учреждениях и т.п. [43].<br>
  <br>
  Немецкая кампания по вербовке добровольцев из крымских татар очень встревожила 
  руководство крымских партизан. Так, начальник II партизанского района И.В. Генов 
  докладывал 31 января 1942 г. на «большую землю»: «Местное татарское население 
  успешно вооружается… немцами, цель — борьба с партизанами… Надо полагать, что 
  в ближайшие дни они начнут практиковаться в борьбе с нами. Мы готовы к этому… 
  хотя понимаем, что вооруженные татары куда опаснее… немцев и румын» [44].<br>
  <br>
  В результате, к весне 1942 г. партизанским отрядам Крыма пришлось бороться фактически 
  на два фронта. По свидетельству заместителя начальника особого отдела Крымского 
  штаба партизанского движения (КШПД) лейтенанта госбезопасности Попова, это был 
  фронт «против немецко-фашистских захватчиков… и фронт против… крымских татар» 
  [45]. А в феврале 1942 г. отдельные отряды добровольцев-татар, численностью 
  до 200—250 человек, были направлены на фронт под Керчь, где приняли участие 
  в боях против Красной Армии. Впоследствии немецкое командование использовало 
  эти отряды под Севастополем [46].<br>
  <br>
  Вербовочная кампания в роты самообороны продолжалась на протяжении февраля—марта 
  1942 г., в результате чего к апрелю 1942 г. их численность достигла 4000 человек 
  при постоянном резерве в 5000 человек [47].<br>
  <br>
  Выступая 24 мая 1942 г. в рейхстаге, Гитлер в своей речи заявил, «что в частях 
  германской армии, наряду с литовскими, латышскими, эстонскими и украинскими 
  легионами, принимают участие в борьбе с большевиками также татарские вспомогательные 
  войска... Крымские татары всегда отличались своей военной доблестью и готовностью 
  сражаться. Однако при большевистском господстве им нельзя было проявить этих 
  качеств… Вполне понятно, что они плечом к плечу стоят с солдатами германской 
  армии в борьбе против большевизма» [48].<br>
  <br>
  В августе 1942 г. начальник Генштаба сухопутных войск генерал-полковник Ф. Гальдер 
  подписал инструкцию за № 8000/42 «Положение о местных вспомогательных формированиях 
  на Востоке», в которой все добровольческие формирования были разделены по категориям 
  согласно их политической благонадежности и боевым качествам. В этом документе 
  представители «тюркских народностей» и казаки выделялись в отдельную категорию 
  «равноправных союзников, сражающихся плечом к плечу с германскими частями против 
  большевизма в составе особых боевых частей», таких, как туркестанские батальоны, 
  казачьи части и крымскотатарские формирования [49].<br>
  <br>
  Татарские добровольцы были также очень горды, что им оказывалось такое доверие. 
  По словам офицера разведки 11-й немецкой армии, «они гордятся тем, что носят 
  немецкую форму» [50].<br>
  <br>
  В первой половине 1942 г. немецкие оккупационные власти в рейхскомиссариатах 
  приступили к созданию из местных добровольцев батальонов «Schuma», которые предполагалось 
  использовать в антипартизанских целях. В отличие от рот самообороны, район действий 
  которых был обычно ограничен районом их формирования, батальоны «Schuma» планировалось 
  применять в качестве тактических частей на более широком фронте.<br>
  <br>
  В июле 1942 г. все татарские роты самообороны в Крыму были сведены в такие батальоны. 
  В результате, к ноябрю 1942 г. было сформировано 8 батальонов «Schuma», расквартированных 
  в следующих населенных пунктах: Симферополе (№№ 147 и 154), Карасубазаре (№ 
  148), Бахчисарае (№ 149), Ялте (№ 150), Алуште (№ 151), Джанкое (№ 152) и Феодосии 
  (№ 153) [51].<br>
  <br>
  В организационном и оперативном отношении все эти части были подчинены фюреру 
  СС и полиции генерального округа «Таврия» СС-бригаденфюреру Л. фон Альвенслебену* 
  [52].<br>
  <br>
  <hr>
  <em>* Альвенслебен, Людольф фон, род. 17.03.1901 г., один из руководителей немецких 
  карательных органов на территории СССР, СС-группенфюрер (1944). В 1920-х гг. 
  примкнул к нацистскому движению. С 4.05.1934 г. – командир 46-го полка СС (Дрезден), 
  затем командовал 26-м полком СС в Галле, 33-м полком в Шверине-Мекленбурге. 
  С ноября 1936 г. – шеф-адъютант рейхсфюрера СС Г. Гиммлера. С 1939 г. – начальник 
  СД и полиции безопасности в Западной Пруссии. В конце 1942 – начале 1943 г. 
  был назначен фюрером СС и полиции генерального округа «Таврида» и пробыл на 
  этой должности до апреля 1944 г.</em><br>
  <hr>
  <br>
  Каждый батальон должен был состоять по штату из штаба и четырех рот (по 124 
  человека в каждой). Каждая рота — из одного пулеметного и трех пехотных взводов. 
  Номинальная численность батальона в 501 человек на практике часто колебалась 
  от 240 до 700 человек [53]. Как правило, батальоном командовал местный доброволец 
  из числа бывших офицеров Красной Армии, однако в каждом из них было еще 9 человек 
  немецкого кадрового персонала (1 офицер связи и 8 унтер-офицеров). На вооружении 
  личный состав батальонов имел автоматы, легкие и тяжелые пулеметы и минометы 
  [54].<br>
  <br>
  11 ноября 1942 г. Главное командование вермахта в Крыму объявило дополнительный 
  набор крымских татар в ряды германской армии «для использования в Крыму». Функции 
  вербовочного бюро выполнял Симферопольский мусульманский комитет (ул. Салгирная, 
  14) [55]. В результате, к весне 1943 г. был сформирован 155-й батальон «Schuma» 
  (Евпатория), а несколько батальонов и хозяйственных рот находились в стадии 
  формирования [56].<br>
  <br>
  В некоторых современных исследованиях указывается, что крымскотатарские батальоны 
  «Schuma» составляли Крымскотатарский легион вермахта [57]. Однако с этим нельзя 
  согласиться. Действительно, в марте 1943 г. на заседании Генштаба сухопутных 
  войск генерал-инспектор восточных войск поднял вопрос об организации такого 
  легиона с последующим использованием его батальонов вне Крыма. Однако командование 
  группы армий «А», в чью тыловую зону входил Крым, высказалось против этого мнения. 
  Более того, оно посоветовало безотлагательно перевести всех крымских татар, 
  служивших в качестве «хиви» в немецких частях других групп армий, обратно в 
  Крым, чтобы в дальнейшем использовать их только здесь [58]. Это было мотивировано 
  тем, что «Крымскотатарский легион не нужен, так как его использование не компенсирует 
  затрат на его формирование» [59].<br>
  <br>
  Каждый батальон проходил стандартную пехотную подготовку, согласно немецким 
  уставам, правда, с «полицейским уклоном». Особую роль при обучении личного состава 
  батальонов играли мероприятия, проводимые Штабом пропаганды «Крым» [60]. После 
  принесения присяги на верность Гитлеру и вручения знамени с национальным татарским 
  гербом каждый батальон отправлялся в предназначенный ему оперативный район [61].<br>
  <br>
  Такими районами батальонов «Schuma» в Крыму были: район Аргин — Баксан — Барабановка, 
  Сартана — Куртлук и Камышлы — Бешуй — батальон № 148; Кокоши — Коуш — Мангуш 
  — батальон № 149; Корбек — Улу-Узень — Демерджи — батальон № 151 [62]. Здесь 
  они несли охрану военных и гражданских объектов, вместе с частями вермахта и 
  немецкой полиции принимали активное участие в поиске партизан. Так, по сводкам 
  немецкого Штаба по борьбе с партизанами, с 9 ноября по 27 декабря 1942 г. батальоны 
  №№ 148, 149 и 150, действовавшие в районах Джанкоя и Карасубазара, участвовали 
  в 6 крупных акциях против партизан. При этом было убито 8 и захвачено 5 партизан. 
  Сколько потеряли батальоны, в сводках не сообщается, скорее всего, их потери 
  были не меньше, чем у партизан [63].<br>
  <br>
  В ряде случаев немецкое командование использовало эти батальоны для проведения 
  карательных акций и для охраны концентрационных лагерей. Например, 4 февраля 
  1942 г. группа татар-добровольцев из деревни Коуш во главе с Ягъей Смаилом совместно 
  с немецким карательным отрядом приняла участие в расправе с жителями поселка 
  Чаир. При этом было зверски убито 15 человек [64].<br>
  <br>
  С весны 1942 г. на территории совхоза «Красный» существовал концентрационный 
  лагерь, где немцы за 2,5 года оккупации замучили и расстреляли не менее 8000 
  жителей Крыма. По свидетельствам очевидцев, лагерь был надежно обнесен двумя 
  рядами колючей проволоки и охранялся татарскими добровольцами из 152-го батальона 
  «Schuma» [65]. Бывший старший лейтенант Красной Армии В. Файнер вспоминал: «Издевательства 
  над военнопленными... не имели предела. Добровольцы-татары вынуждали (какого-нибудь 
  военнопленного. — О.Р.) показывать на себя, что он еврей, затем… выдавали несчастного, 
  за что получали 100 марок» [66].<br>
  <br>
  Неудивительно, что, наблюдая подобные «мероприятия» татар-добровольцев, руководители 
  крымских партизан докладывали в ЦШПД: «Участники партизанского движения в Крыму 
  были живыми свидетелями расправ татар-добровольцев и их хозяев над захваченными 
  больными и ранеными партизанами (убийства, сжигание больных и раненых). В ряде 
  случаев татары были беспощаднее и профессиональнее палачей-фашистов» [67].<br>
  <br>
  Создавая крымскотатарские добровольческие формирования, немецкое военно-политическое 
  руководство преследовало исключительно военные цели, а также стремилось использовать 
  их, как и подобные им формирования, в целях пропаганды. Однако руководство татарских 
  мусульманских комитетов планировало использовать их иначе: чтобы получить от 
  немцев больше политических прав, они рассчитывали превратить эти отряды в своеобразный 
  инструмент давления на своих хозяев.<br>
  <br>
  Так, в апреле 1942 г. группа руководителей Симферопольского мусульманского комитета 
  разработала устав и программу мусульманских комитетов, выдвинув следующие требования:<br>
  <br>
  1. Восстановление в Крыму деятельности партии «Милли-Фирка».<br>
  <br>
  3. Создание Татарской национальной армии.<br>
  <br>
  4. Создание самостоятельного татарского государства под протекторатом Германии 
  [68].<br>
  <br>
  Эта программа была подана на рассмотрение Гитлеру, однако он ее не одобрил. 
  Было позволено лишь увеличить количество добровольцев для вермахта и частей 
  «вспомогательной полиции», результатом чего стало создание батальонов «Schuma».<br>
  <br>
  В мае 1943 г. один из старейших крымскотатарских националистов А. Озенбашлы* 
  написал на имя Гитлера меморандум, в котором изложил следующую программу сотрудничества 
  между Германией и крымскими татарами:<br>
  <br>
  1. Создание в Крыму татарского государства под протекторатом Германии.<br>
  <br>
  2. Создание на основе батальонов «Schuma» и прочих полицейских частей Татарской 
  национальной армии.<br>
  <br>
  3. Возвращение в Крым всех татар из Турции, Болгарии и других государств; «очищение» 
  (можно только догадываться, какими методами. — О.Р.) Крыма от других национальностей.<br>
  <br>
  4. Вооружение всего татарского населения, включая глубоких стариков, вплоть 
  до окончательной победы над большевиками.<br>
  <br>
  5. Опека Германии татарского государства, пока оно сможет «встать на ноги» [69].<br>
  <br>
  Главным сходством этой программы с предыдущей было требование создать «Татарскую 
  национальную армию». По замыслам Озенбашлы, «в случае если Германия в результате 
  войны окажется обессиленной», то при помощи хорошо вооруженной и обученной армии 
  можно будет «требовать у нее самостоятельности» [70].<br>
  <br>
  <hr>
