  <br>
  Карабаш, Абдулла, в 30-х гг. окончил Симферопольский пединститут и аспирантуру, 
  после чего занимал пост референта при Совете Народных Комиссаров Крыма (до октября 
  1941 г.). После начала немецкой вербовочной кампании в январе 1942 г. стал одним 
  из главных вербовщиков крымскотатарских добровольцев. В мае 1944 г. переехал 
  в Германию. Командир боевой группы «Крым» Восточно-тюркского соединения СС в 
  чине ваффен-гауптштурмфюрера (с 14.12.1944 г.). С марта 1945 г. – представитель 
  всех крымскотатарских добровольцев при Крымскотатарском национальном центральном 
  комитете.<br>
  <br>
  Аппаз, Гжик, в 1920–21 гг. был главой крымскотатарских антисоветских формирований, 
  за что впоследствии осужден.</em><br>
  <hr>
  <br>
  Все мероприятия по набору добровольцев должны были проводиться согласно директиве 
  генерал-квартирмейстера Генштаба сухопутных войск Е. Вагнера от 18 января 1942 
  г. В ней разрешалась «неограниченная» организация крымскотатарских формирований 
  на территориях, «находившихся в немецких руках, за исключением Керченского полуострова 
  и района осады Севастополя» [28].<br>
  <br>
  Вербовка добровольцев проводилась в течение января 1942 г. в 203 населенных 
  пунктах и 5 лагерях военнопленных. В результате было набрано 9255 человек (наибольшее 
  количество добровольцев в Крыму дал Карасубазар — 1000 человек, наименьшее — 
  Биюк-Онлар — 13 человек), из которых в части 11-й армии было направлено 8684 
  человека, а остальные, признанные негодными для службы в строевых частях, направлялись 
  в свои деревни.<br>
  <br>
  Одновременно с этим по линии айнзатцгруппы «Д» было завербовано 1632 человека, 
  которые были сведены в 14 рот самообороны, расквартированных соответственно 
  их порядковым номерам в следующих населенных пунктах: Симферополе, Биюк-Онларе, 
  Бешуе, Баксане, Молбае, Бий-Ели, Алуште, Бахчисарае, Коуше, Ялте, Таракташе 
  (12-я и 13-я роты) и Джанкое [29].<br>
  <br>
  Каждая татарская рота самообороны состояла из трех взводов и насчитывала от 
  50 (Джанкой) до 175 (Ялта) человек. Ротами командовали немецкие офицеры. Бойцы 
  рот были одеты в стандартное немецкое полицейское обмундирование, но без знаков 
  различия. На вооружении у личного состава находилось стандартное пехотное оружие, 
  в основном легкое. Впоследствии также поступили тяжелые пулеметы и минометы 
  [30].<br>
  <br>
  При создании подобных формирований немецкое командование помимо чисто военных 
  целей рассчитывало и на определенный пропагандистский эффект. По его замыслам, 
  партизаны должны были увязнуть «в борьбе не с немцами, а с формированиями из 
  местного населения» [31]. По словам начальника Центрального штаба партизанского 
  движения (ЦШПД) П.К. Пономаренко, вокруг этих формирований велась «бешеная националистическая 
  пропаганда», ей сопутствовало «разжигание национальной розни, антисемитизма» 
  [32].<br>
  <br>
  Существенную роль в идеологической обработке татарских добровольческих формирований 
  играл отдел культуры и религии Симферопольского мусульманского комитета (руководитель 
  — Э. Гафаров*) и подобные отделы в комитетах на местах. Особый акцент при этом 
  делался на воспитание «добровольческой молодежи, которая получила большевистское 
  образование, на примерах турецко-татарской истории» [33].<br>
  <br>
  При этом главная роль проводника крымскотатарской националистической идеологии 
  отводилась газете «Azat Kirim» («Свободный Крым»), которая начала выходить с 
  11 января 1942 г. Эта газета являлась органом Симферопольского мусульманского 
  комитета и выходила два раза в неделю на татарском языке (адрес редакции: ул. 
  Пушкинская, 14; печаталась в типографии Симферопольского городского управления 
  по ул. Салгирной, 23) [34]. Вначале газета выходила небольшим тиражом, однако 
  в связи с директивами Штаба пропаганды «Крым» (главного инструмента немецкой 
  психологической войны в Крыму) по усилению пропагандистского воздействия на 
  местное население ее тираж летом 1943 г. вырос до 15 тысяч экземпляров [35].<br>
  <br>
  Долгое время ее главным редактором и автором всех передовых статей был М. Куртиев*. 
  Среди других сотрудников редакции газеты следует назвать Ф. Абляева* (автора 
  статей по «женскому вопросу»), А. Куркчи* (автора националистических пропагандистских 
  статей, фельетонов и передовиц), Н. Сейдаметова* (автора корреспонденций по 
  сельскохозяйственным вопросам) и М. Низами (автора материалов по вопросам культуры 
  и пропаганды; самой известной из его статей на эту тему была статья «Вопросы 
  совести и большевизма») [36].<br>
  <br>
