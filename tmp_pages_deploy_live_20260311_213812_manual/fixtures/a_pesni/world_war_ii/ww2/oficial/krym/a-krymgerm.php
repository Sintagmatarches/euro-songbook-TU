  – народного комиссара финансов и заместителя народного комиссара финансов. Репрессирован 
  по делу Вели Ибраимова. После отбытия наказания работал врачом в Павлодаре. 
  В первой половине 1943 г. вернулся в Крым, где начал фракционную борьбу с председателем 
  Симферопольского мусульманского комитета Абдурешидовым. Сторонники Озенбашлы 
  в комитете (С. Аметов, А. Куркчи, Э. Курсеитов) предлагали немцам назначить 
  его муфтием мусульман Крыма. </em><br>
  <hr>
  <br>
  Однако выполнение подобных требований не входило в планы нацистского руководства, 
  поэтому крымское гестапо сочло «более благоразумным» не давать ход этому меморандуму. 
  Гитлер о нем так и не узнал.<br>
  <br>
  Понимая всю важность стратегического положения Крыма как выгодного плацдарма 
  для наступления на Кавказ, немецкое верховное командование не желало видеть 
  здесь какие-либо «национальные армии», помимо вермахта и союзных румынских частей. 
  Поэтому все попытки лидеров крымскотатарских националистов «использовать обстоятельства 
  совместной борьбы в своих целях», попытки создать «Татарскую национальную армию» 
  были заранее обречены на провал всей логикой немецкой оккупационной политики. 
  «…Повсюду оккупанты твердо держали власть в своих руках и свирепо подавляли 
  малейшие попытки к обретению… национальной самостоятельности», — писали американские 
  историки М.Я. Геллер и А.М. Некрич [71].<br>
  <br>
  После отступления немцев с Кавказа и блокирования их крымской группировки в 
  крымскотатарских частях начался процесс разложения, участились случаи перехода 
  на сторону партизан, наиболее значительным из которых стал переход 152-го батальона 
  «Schuma» под командованием уже упоминавшегося майора Раимова. Существенный приток 
  татар в партизанские отряды начался осенью 1943 г. К декабрю их перешло к партизанам 
  406 человек, из которых 219 служили до этого в батальонах «Schuma» или в самообороне 
  [72].<br>
  <br>
  Под влиянием массово-агитационной работы партизан среди татарского населения 
  и в добровольческих частях во многих батальонах осенью–зимой 1943 г. были созданы 
  просоветские подпольные организации. Так, командир 154-го батальона А. Керимов 
  был арестован немцами «как неблагонадежный», в 147-м батальоне 76 человек были 
  арестованы и расстреляны «как просоветский элемент» [73], но уже в январе 1944 
  г. начальник штаба этого батальона Кемалов готовил его к переходу на сторону 
  Красной Армии. Единственным препятствием, которое все-таки заставляло его колебаться, 
  было предположение, высказанное им при встрече с партизанским связным: «…Даже 
  если… весь отряд выполнит это задание, все равно после занятия Симферополя (Красной 
  Армией. — О.Р.) их поодиночке всех накажут» [74].<br>
  <br>
  В результате, по немецким данным, около 1/3 батальонов «Schuma» оказались ненадежными 
  и были разоружены самими немцами, а их личный состав помещен в концлагеря [75]. 
  Остальные же батальоны, в которых, по мнению руководства КШПД, служили «настоящие 
  добровольцы, бывшие недовольные советской властью элементы» [76], в апреле—мае 
  1944 г. сражались против освобождавшей Крым Красной Армии. Так, по воспоминаниям 
  И.И. Купреева, комиссара 5-го отряда 6-й бригады Восточного соединения, добровольцы 
  из бахчисарайского батальона «Schuma» очень упорно сражались за город, а после 
  освобождения Бахчисарая многие татары прятали в своих домах уцелевших немцев 
  [77].<br>
  <br>
  После разгрома в мае 1944 г. крымской группировки немцев было решено свести 
  все уцелевшие крымскотатарские батальоны «Schuma» в трехбатальонный Татарский 
  горно-егерский полк СС (Tataren-Gebirgsjдger-Regiment der SS). Полк проходил 
  подготовку на учебном полигоне Мурлагер (Германия), где 8 июля 1944 г. приказом 
  Главного управления СС был развернут в Татарскую горно-егерскую бригаду СС № 
  1 (Waffen-Gebirgsjдger-Brigade der SS (tatarische № 1). В середине июля 1944 
  г. бригада, которая находилась еще в стадии формирования, покинула Германию 
  и была переведена в Венгрию, где ей следовало проходить дальнейшую подготовку, 
  а также нести гарнизонную службу [78].<br>
  <br>
  В этот период бригада имела следующий численный состав: 11 офицеров, 191 унтер-офицер 
  и 3316 рядовых — всего 3518 человек, из которых около 1/4 составляли немцы, 
  переведенные, главным образом, из военной полиции. Командиром бригады на всем 
  протяжении ее существования являлся СС-штандартенфюрер В. Фортенбахер [79].<br>
  <br>
  В октябре 1944 г. Главное управление СС отдало приказ о формировании «соединения 
  дивизионного типа» из остатков принимавшего участие в подавлении в августе 1944 
  г. Варшавского восстания 1-го Восточно-мусульманского полка СС. Эта часть должна 
  была носить наименование Восточно-тюркское соединение СС (Osttürkische Waffenverband 
  der SS) и организационно состоять из боевых групп (Waffengruppe): «Туркестан» 
  (включала добровольцев из Средней Азии и Казахстана), «Идель-Урал» (включала 
  добровольцев — поволжских татар), «Азербайджан», а затем и «Крым». Последняя 
  вошла в состав соединения 31 декабря 1944 г. и состояла из упоминавшейся выше 
  горно-егерской бригады, которая была к этому времени расформирована. Группа 
  включала в себя штаб, два пехотных батальона и две отдельные артиллерийские 
  роты. Ее командиром был назначен уже упоминавшийся ваффен-гауптштурмфюрер (офицер 
  «негерманских» частей войск СС носил в своем звании приставку «ваффен-».) А. 
  Карабаш [80].<br>
  <br>
  Кроме того, при штабе Восточно-тюркского соединения имелось несколько офицеров, 
  которые исполняли обязанности связных с Главным управлением СС. Одним из них 
  в чине ваффен-оберштурмфюрера был крымский татарин И. Даирский* [81].<br>
  <br>
  <hr>
  <em>* Даирский, Искандер, ваффен-оберштурмфюрер, из советских военнопленных, 
  до 1944 г. служил в 1-м Восточно-мусульманском полку СС, когда в связи с ранением 
  прибыл в Берлин, где исполнял обязанности офицера связи Главного управления 
  СС с Восточно-тюркским соединением СС и «Крымским центром» органом крымскотатарской 
  эмиграции, созданном при Министерстве оккупированных восточных областей А. Розенберга.</em><br>
  <hr>
  <br>
  Часть крымскотатарских добровольцев была переброшена во Францию и включена в 
  состав запасного батальона Волжско-татарского легиона, дислоцировавшегося в 
  г. Ле-Пюи. Здесь этот батальон участвовал в репрессиях против мирного населения 
  и боях против французских партизан-маки [82].<br>
  <br>
  Еще 831 человек из состава крымскотатарских частей в конце 1944 г. был направлен 
  в качестве «хиви» в ряды 35-й полицейской гренадерской дивизии СС, где из них 
  были сформированы следующие подразделения:<br>
  <br>
  - 2-я тяжелая дивизионная транспортная колонна. В ней под командой обер-лейтенанта 
  Аслана Кугушева служило 3 офицера, 2 военных чиновника и 125 добровольцев;<br>
  <br>
  - 7-я гренадерская рота 91-го полицейского гренадерского полка. В ней под командой 
  ваффен-оберфюрера А.П. Ширинского служило 7 офицеров, военный мулла и 252 добровольца;<br>
  <br>
  - 3-я гренадерская рота 89-го полицейского гренадерского полка. В ней под командой 
  зондерфюрера (в звании ротного командира) Мустафы Тайганского служило 4 офицера, 
  1 военный чиновник и 382 добровольца;<br>
  <br>
  - боевая полицейская группа «147». В ней под командой СС-унтерштурмфюрера Митте 
  служило 2 офицера (переводчик и офицер связи) и 52 добровольца [83].<br>
  <br>
  Часть же эвакуированной из Крыма татарской молодежи была зачислена в состав 
  вспомогательной службы ПВО [84].<br>
  <br>
  После окончания Второй мировой войны по договору с западными союзниками все 
  эти добровольцы были выданы в СССР.<br>
  <br>
  Таким образом, можно сказать, что за период с 1941 по 1945 гг. в германских 
  вооруженных силах прошли службу от 15 до 20 тысяч крымских татар-добровольцев 
  из общей численности крымскотатарского населения в 218 тысяч человек (по данным 
  на 1939 г.). Это составляет примерно 1/20 от общего количества мусульман-добровольцев 
  из граждан СССР (около 400 тысяч). Эти цифры подтверждаются такими авторитетными 
  немецкими историками, как М. Лютер, И. Хоффманн и Г.-В. Нойлен [85].<br>
  <br>
  Для сравнения, в 1941 г. в ряды Красной Армии было призвано 10 тысяч татар, 
  большинство из которых так и не попало на фронт, а осталось в Крыму. В 1941—1944 
  гг. в партизанских отрядах Крыма сражалось 1130 татар (общее количество партизан 
  за этот период — около 11 тысяч), из них погибло 96, пропало без вести 103 и 
  дезертировало 177. В подпольных организациях Крыма за тот же период татар было 
  менее 100 человек (всего крымских подпольщиков насчитывалось около 2500 человек) 
  [86].<br>
  <br>
  Начальник немецкого Генштаба сухопутных войск генерал-полковник Ф. Гальдер писал 
  в своем дневнике за 8 августа 1941 г., что «…исходя из имеющегося опыта… на 
  каждый миллион населения можно сформировать две дивизии…» [87]. Если учесть, 
  что на 22 июня 1941 г. численность немецкой пехотной дивизии составляла 15859 
  человек [88], то получится, что немецкие оккупационные власти «учли» значительную 
  часть боеспособного крымскотатарского населения.<br>
  <br>
  Долгое время в отечественной историографии бытовало мнение, высказанное М.И. 
  Калининым 4 августа 1943 г. на встрече с фронтовыми агитаторами, работающими 
  среди бойцов нерусских национальностей. Говоря об изменниках Родины, перешедших 
  на сторону врага, он сказал: «Встречаются ничтожные исключения, которые для 
  такой большой страны, как СССР, не имеют значения» [89].<br>
  <br>
  Однако приведенные выше цифры говорят об обратном.<br>
  <br>
  Начиная войну против СССР, военно-политическое руководство третьего рейха одной 
  из главных своих задач считало разрушение многонационального государства путем 
  привлечения на свою сторону в борьбе с «большевизмом и московским империализмом» 
  представителей нерусских народов и национальных меньшинств нашей страны. Особая 
  ставка при этом делалась на многочисленные мусульманские народы СССР. «Одним 
  из способов привлечения на сторону Германии этих народов, — считает современный 
  российский исследователь С.И. Дробязко, — и стало создание национальных «восточных» 
  легионов в качестве ядра армий будущих «независимых» государств» [90].<br>
  <br>
  Более того, Гитлер, относясь крайне скептически к созданию подобных добровольческих 
  формирований из славянских народов, тем не менее не возражал против предложений 
  о создании мусульманских легионов: «…Надежными я считаю только мусульман… и 
  не вижу опасности в создании чисто мусульманских частей…» — заявил он на совещании 
  в декабре 1942 г. [91].<br>
  <br>
  В составе этих добровольческих частей находились, в большинстве своем, не случайные 
  жертвы войны, которые не имели возможности уклониться от службы у оккупантов 
  или мечтали при первом удобном случае перебежать на сторону Красной Армии, а 
  прежде всего убежденные националисты. Эмигрантский мемуарист А.С. Казанцев оставил 
  следующее свидетельство о «политическом воспитании» этих добровольцев: «В порядке 
  выполнения планов дележа (России. — О.Р.) из военнопленных (и добровольцев) 
  разных национальностей создавались батальоны, которые воспитывались в звериной 
  ненависти не только и не так к большевизму, как ко всему русскому… искусственно 
  раздувался уродливый, злобный шовинизм» [92].<br>
  <br>
  Поэтому на фоне такого «идеологического воспитания» крайне нелепо выглядят заявления 
  немецкой пропаганды и ее подпевал из числа националистов: «…В боях против большевизма 
  родилась великая дружба народов. И тот, кто старается посеять национальную рознь… 
  играет на руку большевикам и является нашим врагом» [93].<br>
  <br>
  Это «новое оружие» третьего рейха в лице мусульманских добровольческих формирований 
  активно заявляло о себе не только на передовой, но и в тылу, где эти части охотились 
  на партизан, проводили карательные акции против мирного населения, уничтожали 
  своих соотечественников и граждан других стран. Пепелища и кровь — вот что оставляли 
  после себя мусульманские формирования, впрочем, как и прибалтийские, и украинские, 
  и кавказские. Нечто подобное имело место и в Крыму в период оккупации.<br>
  <br>
  Все вышесказанное важно не само по себе, а в отношении тех задач, которые решали 
  с использованием мусульманских добровольцев, и в том числе крымских татар, правители 
  нацистской Германии. В своем стремлении добиться мировой гегемонии они старались 
  показать себя друзьями мусульман, завоевать их расположение и использовать их 
  в своих целях. Однако подобные попытки не ушли в прошлое — в наше время мы можем 
  наблюдать нечто подобное. Поэтому следует помнить, что эти события не только 
  интересны, но весьма поучительны и актуальны.<br>
