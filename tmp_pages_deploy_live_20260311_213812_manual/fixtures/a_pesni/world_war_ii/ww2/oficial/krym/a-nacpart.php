	 <strong>Олег Романько,<br> 
	 кандидат исторических наук, доцент кафедры украиноведения Крымского 
  государственного медуниверситета им. С.И. Георгиевского (Симферополь)</strong><br>
  <br>
<span class="header"><strong>БОРЬБА ЗА НАЦИОНАЛЬНОЕ ОСВОБОЖДЕНИЕ, ИЛИ ГРАЖДАНСКОЕ 
  ПРОТИВОСТОЯНИЕ: К ВОПРОСУ О НАЦИОНАЛИСТИЧЕСКОМ ПАРТИЗАНСКОМ ДВИЖЕНИИ В ГОДЫ 
  ВТОРОЙ МИРОВОЙ ВОЙНЫ</strong><br></span>
  <br>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=100&par=74&l=&art=533" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, №16, 
  2006</em></a>.<br>
  <br>
  <br>
  14 октября 2006 года президент Виктор Ющенко подписал Указ «О всестороннем изучении 
  и объективном освещении деятельности украинского освободительного движения и 
  содействии процессу национального примирения». Судя по названию этого документа, 
  в нем должна идти речь обо всех тех, кто, по мнению Президента и его советников, 
  «боролся за свободу Украины» — от деятелей Украинской центральной Рады и Директории 
  до диссидентов. Однако не вызывает сомнений, что главной целью этого указа является 
  реабилитация очень неоднозначной в нашей истории организации. Речь идет о так 
  называемой Украинской повстанческой армии (УПА) — о детище и военном крыле Организации 
  украинских националистов (ОУН). Даже при большом желании нельзя признать это 
  значимым фактом в истории Второй мировой войны. Тем не менее, есть ряд моментов, 
  которые до сих пор не позволяют отнести его к сугубо научным вопросам, а придают 
  ему актуальность и мешают объективному изучению.<br>
  <br>
  Идейные наследники и современные апологеты этих организаций позиционируют их 
  как «третью силу», которая в годы немецкой оккупации «боролась и с нацистами, 
  и с коммунистами», а после освобождения Украины «сражалась против большевистской 
  оккупации за свободу украинского народа». Отсюда следует требование признать 
  ныне живущих членов УПА «воюющей стороной» со всеми вытекающими последствиями: 
  социальными льготами, ветеранскими пенсиями и т.п. Более того, уже на уровне 
  государственной власти к советским ветеранам ВОВ следуют обращения о примирении 
  с «воинами УПА» по примеру Испании, где подобное имело место между бывшими противниками 
  по гражданской войне 1936—1939 годов.<br>
  <br>
  Антагонисты этой точки зрения настаивают на том, что УПА была создана немцами, 
  снабжалась ими и воевала полностью на их стороне. Естественно, что при таком 
  подходе, ни о каком уравнении в правах ее членов с ветеранами Советской армии 
  и речи быть не может. Говорить же о каком-то примирении и вовсе аморально.<br>
  <br>
  В современных социально-политических условиях эта дискуссия приобрела отчетливые 
  черты политического инструмента, которые та и другая сторона используют по своему 
  усмотрению, настаивая на своей правоте и полностью отвергая позицию оппонента.<br>
  <br>
  Для жителей же большей части Украины этот спор абсолютно не актуален. Согласно 
  данным социологических опросов, этой проблемой интересуется только 15% ее населения. 
  А по-иному и не может быть! Те, кто ратует за немедленное признание воинов УПА 
  участниками войны, исходят из того, что эта армия якобы воевала за свободу и 
  независимость всей Украины в ее этнографических границах — «от Сана до Дона», 
  то есть это была общеукраинская армия. Однако факты говорят о другом. Даже простого 
  взгляда на карту деятельности УПА достаточно, чтобы понять: это была организация, 
  которая так и не вышла за пределы Галиции и Волыни. А те жалкие попытки закрепиться 
  на остальной территории Украины, которые предпринимали некоторые ее руководители, 
  так и остались попытками — по причине полного равнодушия местного населения 
  к галицийской версии украинского национализма. Не секрет, что эта идеология 
  здесь чужда и сейчас. Поэтому все связанное с УПА превратилось в элемент наступления 
  так называемой западенской идеологии, частью которой и является история этой 
  армии, на «русифицированный Восток». Естественно, что такая идеологическая агрессия 
  вызывает у жителей Юго-Восточной Украины ответную реакцию. Пока тоже идеологическую, 
  так как навязывание своей точки зрения в такой форме может привести только к 
  эскалации напряженности. И если на Западной Украине на этот вопрос смотрят исключительно 
  через «бандеровскую» призму, то на юго-востоке проблема ОУН—УПА освещается с 
  позиций прежней коммунистической идеологии и науки, которые тоже не были заинтересованы 
  в ее правдивом изучении.<br>
  <br>
  Нельзя сказать, что данный вопрос не анализируется в современной украинской 
  исторической науке: выходят сотни книг и статей, многие из которых написаны 
  на уникальном архивном материале. Однако бинарный взгляд на этот вопрос накладывает 
  свой отпечаток и на соответствующую литературу. В результате проблема УПА воспринимается 
  исключительно в черно-белых тонах: или безудержное восхваление, или полное ниспровержение.<br>
  <br>
  Президент и другие официальные лица просят нас понять и принять эти события, 
  игнорируя детали, которые в результате оказываются самыми значимыми для понимания 
  исследуемого феномена.<br>
  <br>
  ***<br>
  <br>
  История УПА рассматривается у нас под таким углом зрения, который уже предопределяет 
  ее ошибочное толкование. Назовем две главные методологические ошибки, которые 
  допускаются при обращении к этой теме:<br>
  <br>
  - она трактуется как часть проблемы коллаборационизма;<br>
  <br>
  - во многих работах подразумевается некая уникальность УПА (этим «грешат» и 
  коммунисты, и националисты).<br>
  <br>
  Прежде всего обратимся к термину «коллаборационизм». Считаем корректным следующее 
  определение: это добровольное сотрудничество с нацистским военно-политическим 
  руководством на территории Германии или оккупированных ею стран с целью личной 
  выгоды или укрепления установленного административно-политического режима. При 
  этом личная выгода, естественно, могла быть не только экономического характера 
  [1]. Из этого следует, что коллаборационизм — это вполне определенное явление, 
  которое возникло в годы Второй мировой войны, и не без влияния некоторых довоенных 
  событий. Оно является неотъемлемой частью истории этой войны, без изучения которой 
  наши знания о ней будут неполными.<br>
  <br>
  История оккупации, помимо коллаборационизма, имеет и другую сторону — движение 
  Сопротивления. Это очень общее название охватывает все формы борьбы с оккупантами: 
  от саботажа до активного вооруженного сопротивления — партизанской войны. Последняя 
  интересует нас больше всего, так как УПА фактически и была такой партизанской 
  армией. В связи с этим трудно не согласиться с немецким историком Берндтом Бонвечем, 
  который утверждал, что «вопрос о поддержке партизан населением по сути дела 
  является оборотной стороной вопроса о готовности к коллаборационизму» [2].<br>
  <br>
  <img src="a-nacpart1.jpg" width="198" height="105"><br>
  <br>
  Одной из причин непонимания проблемы УПА является отнесение этой армии к числу 
  коллаборационистских формирований, которые во множестве были созданы немцами 
  за период войны. Проблема УПА — это часть истории движения Сопротивления, которая, 
  как выясняется, «обросла» различными мифами не меньше, чем проблема коллаборационизма.<br>
  <br>
  Мы привыкли считать, что все жители оккупированных территорий к немцам были 
  настроены враждебно. И если не пошли в партизаны, то уж вредили оккупантам, 
  чем могли. Такое утверждение является первым мифом, так как в реальности население 
  было поляризовано: самые активные отчасти стали коллаборационистами, отчасти 
  ушли в Сопротивление. Основная же масса оставалась пассивной. И фактически борьба 
  за симпатии этой массы и представляет основной аспект истории оккупации. Проходила 
  она как идеологически, так и физически, с оружием в руках. Иногда не столько 
  против немцев, сколько против своих соотечественников. Сказанное позволяет нам 
  рассматривать события оккупации в целом ряде стран Европы как гражданское противостояние.<br>
  <br>
  Долгое время не подлежало сомнению, что антинацистское партизанское движение 
  было исключительно коммунистическим. Тем не менее, это есть миф номер два.<br>
  <br>
  <img src="a-nacpart2.jpg" width="198" height="263"><br>
  <br>
  Наконец, советские историки всегда утверждали, что любые «буржуазные националисты» 
  (а под ними подразумевались, за редким исключением, все коммунисты) не являлись 
  самостоятельной силой в годы войны, а были только «немецкими марионетками». 
  Все некоммунистические и националистические движения уже по определению не могли 
  быть враждебными немцам, и тем более воевать против них. Если же наличие иного, 
  некоммунистического подполья и признавалось, то оно объявлялось полностью зависимым 
  от оккупантов и «далеким от народа». Хотя ситуация в целом ряде случаев была 
  много сложнее, чем ее представляли советские (и западные историки, которые впадали 
  в другую крайность — полную апологетику некоммунистических партизан), эти утверждения 
  послужили основой еще одного, третьего мифа, который до конца не развенчан и 
  сейчас.<br>
  <br>
  <img src="a-nacpart3.jpg" width="198" height="277"><br>
  <br>
  Отмеченное скорее даже не мифы, а целые их комплексы, которые, взаимодействуя, 
  затрагивают почти все стороны партизанского движения: его причины, цели и задачи, 
  действующие силы, масштабы, эффективность, отношения с местным населением и 
  многое другое. <br>
  <br>
  <img src="a-nacpart4.jpg" width="198" height="249"><br>
  <br>
  В той или иной форме, с разной степенью развития партизанское движение существовало 
  во всех оккупированных немцами странах Европы. Где-то оно (как, например, во 
  Франции и Италии) было весьма значительным, но до высадки западных союзников 
  в Нормандии и на Апеннинах не играло сколько-нибудь серьезной роли. В Дании 
  и Норвегии партизанское движение так и не вышло за пределы небольших отрядов, 
  которые также не приносили значительного вреда оккупантам. В Восточной и Юго-Восточной 
  Европе (Польша, Югославия, Греция, Албания) со временем оно приобрело такой 
  размах, что здесь правильнее говорить уже не о движении Сопротивления, а национально-освободительной 
  войне, перешедшей в ряде случаев в социальную революцию и гражданскую войну. 
  Гораздо сложнее вопрос о партизанском движении на территории СССР, так как в 
  силу целого ряда факторов некоторые его проявления следует рассматривать как 
  продолжение гражданской войны 1918—1922 годов (аналогично нужно трактовать и 
  советский коллаборационизм). С другой стороны, являясь в какой-то степени спонтанным, 
  оно находилось под значительным влиянием регулярных военных структур Советского 
  Союза и Германии. Все эти отличия были обусловлены причинами политического, 
  идеологического, исторического, национального, религиозного и военного характера, 
  которые и привели к тому или иному ходу развития партизанского движения [3]. 
  Но у всех этих разновидностей был объединяющий фактор, в котором можно выделить 
  два направления. Назовем их условно прокоммунистическое и некоммунистическое. 
  Такие конкурирующие направления существовали во всех партизанских движениях. 
  Однако не везде их конкуренция приводила к гражданскому противостоянию и сотрудничеству 
  с оккупантами, что впоследствии и дало основание коммунистическим историкам 
  обвинять эти движения в коллаборационизме и «предательстве народных интересов».<br>
  <br>
  Проанализировав вышеприведенные факты, выделим на территории Восточной и Юго-Восточной 
  Европы государства, где партизанская война приобрела формы гражданского противостояния. 
  Это, прежде всего, Польша, Югославия, Греция, Албания и Прибалтика, Белоруссия 
  и Украина на территории СССР. Нет нужды говорить, что в этих странах существовало 
  влиятельное прокоммунистическое партизанское движение. Тем не менее, с 1939 
  по 1945 год на их территории действовал целый ряд партизанских организаций, 
  члены которых никак не считали себя адептами коммунистической идеологии. Назовем 
  наиболее крупные объединения, которые сыграли значительную роль в развернувшемся 
  противостоянии: <br>
  <br>
  <img src="a-nacpart5.jpg" width="198" height="312"><br>
  <br>
  Польша: Армия Крайова (АК) — партизанские формирования сторонников лондонского 
  эмигрантского правительства;<br>
  <br>
  Югославия: Движение Четников (или просто Четники) — партизанские формирования 
  сторонников лондонского эмигрантского правительства;<br>
  <br>
  Греция: Национально-демократическая лига Греции (ЭДЭС), созданная как из правых 
  республиканцев, так и из сторонников восстановления греческой монархии; <br>
  <br>
  <img src="a-nacpart6.jpg" width="198" height="282"><br>
  <br>
  Албания: организация «Легалитет», созданная из албанских монархистов, и организация 
  «Национальный фронт», объединившая правых республиканцев, настроенных антимонархически;<br>
  <br>
  Прибалтика: различные организации сторонников возрождения независимости Эстонии, 
  Латвии и Литвы, наиболее значительной из которых была Литовская освободительная 
  армия (ЛОА); <br>
  <br>
  <img src="a-nacpart7.jpg" width="198" height="297"><br>
  <br>
  Белоруссия: различные антикоммунистические организации сторонников независимости 
  Белоруссии, которые в период войны носили обобщающее название Белорусского народного 
  партизанского движения (так называемая «партизанка»), а после окончания оккупации 
  — Белорусской освободительной армии (БОА); <br>
  <br>
  <img src="a-nacpart8.jpg" width="198" height="251"><br>
  <br>
  Украина: Украинская народно-революционная армия (УНРА) атамана Тараса Бульбы-Боровца, 
  в которую входили сторонники восстановления Украинской народной республики образца 
  1918—1921 годов, вооруженные формирования мельниковского крыла ОУН и уже упоминавшаяся 
  УПА, созданная под эгидой и идеологическим влиянием бандеровского крыла ОУН 
  [4]. <br>
  <br>
  <img src="a-nacpart9.jpg" width="198" height="280"><br>
  <br>
  Простое перечисление партизанских организаций и стоявших за ними политических 
  сил свидетельствует, что все они имели влияние в определенных кругах населения 
  своих стран. Иначе эти движения попросту не продержались бы такое значительное 
  время (все они в том или ином виде благополучно дожили до 1945 года, а некоторые 
  продолжали действовать и после капитуляции Германии). Это свидетельствует о 
  том, что в основе их появления лежали более серьезные причины, чем просто злая 
  воля «монархической (или буржуазной) реакции, вступившей в сговор с оккупантами 
  на почве антикоммунизма». <br>
  <br>
  <img src="a-nacpart10.jpg" width="198" height="300"><br>
  <br>
  Для каждой из указанных стран действовали свои причины, которые в целом можно 
  сгруппировать в такие комплексы: исторический, политический, идеологический, 
  национальный, религиозный и военный. Они определили условия возникновения и 
  динамику развития любой разновидности партизанского движения. Под условиями 
  мы понимаем идеологическую основу, наличие необходимого количества обученных 
  кадров, контингента, вооружения и т.п. Это внутренние факторы. К внешним же 
  следует отнести помощь того или иного государства (например, Германии, СССР 
  или западных союзников).<br>
  <br>
  Любая партизанская организация должна иметь идеологическую базу, определяющую 
  те цели и задачи, за которые ее члены борются. Иначе это уже не партизаны, а 
  обыкновенные бандиты. Не были исключением и все вышеперечисленные партизанские 
  движения. Каждое из них в своей борьбе опиралось на тот набор идей, который 
  ее участникам представлялся наиболее приемлемым. В советской историографии было 
  принято выделять только две из них: национализм и антикоммунизм. Однако это 
  не так. Практически все некоммунистические партизанские движения в Восточной 
  и Юго-Восточной Европе придерживались той официальной идеологии, которая существовала 
  в их государствах на момент оккупации. Национализм же (с существенной религиозной 
  окраской) играл значительную роль только в польском и югославском движении Сопротивления 
  (ниже мы увидим почему). Антикоммунизм же вышел здесь на первое место только 
  тогда, когда гражданское противостояние в борьбе этих организаций уступило место 
  борьбе против оккупантов (примерно с 1942 года). Действительно же националистическими 
  и антикоммунистическими можно назвать только партизанские формирования на территории 
  СССР [5].<br>
  <br>
  Что касается обученных кадров, то с этих позиций в более благоприятном положении 
  находились АК и Четники, основной костяк которых составили офицеры и унтер-офицеры 
  бывших польской и югославской армий. В значительно худшем положении находились 
  греческие и албанские партизаны. На территории СССР благоприятно ситуация сложилась 
  для прибалтийских партизан, в составе которых числилось много офицеров и унтер-офицеров 
  бывших эстонской, латвийской и литовской армий. Белорусские и украинские партизаны 
  такого кадрового состава не имели вообще. В лучшем случае это были перебежчики 
  из Красной армии и офицеры и унтер-офицеры прежней польской армии. В кадровый 
  состав белорусского и украинского партизанского движений следует также включить 
  офицеров армий Украинской и Белорусской народных республик, существовавших с 
  1918 по 1921 год. Естественно, что уровень их знаний и умений мало отвечал требованиям 
  современной войны. Поэтому вскоре перед руководством этих движений возник вопрос 
  о подготовке таких кадров. В ходе войны и после 1945 года здесь наиболее преуспели 
  литовские и украинские партизаны. Кроме того, все эти движения имели еще один 
  источник своего кадрового пополнения: начиная с конца 1942 года, к ним стали 
  перебегать члены созданных немцами коллаборационистских формирований. Например, 
  такое часто имело место на Украине в ходе всего периода оккупации. В Прибалтике 
  же этот процесс стал массовым только перед уходом немцев [6].<br>
  <br>
  И прокоммунистическое, и некоммунистическое партизанское движение в той или 
  иной степени поддерживалось внешними силами. Разумеется, в наилучшем положении 
  находились советские партизаны, которые, при всем неоднозначном отношении к 
  ним сталинского руководства, все-таки более или менее снабжались и обученными 
  кадрами, и вооружением, и снаряжением. Что касается их коллег в Югославии, Албании 
  и Греции, то они получали поддержку в основном от западных союзников. И то не 
  сразу, так как основная помощь последних была направлена на некоммунистические 
  формирования. И только после того, как, например, Четники и албанский «Национальный 
  фронт» были обвинены в сотрудничестве с оккупантами, Англия и США прекратили 
  помощь им. Из сказанного следует, что оккупанты помогали некоммунистическим 
  партизанам. На Балканах этим занимались Германия и отчасти Италия. На территории 
  СССР — только Германия [7].<br>
  <br>
  Каждое из партизанских движений имело свою динамику развития. В силу перечисленных 
  причин они значительно отличались друг от друга как по целям, так и по организации 
  и численности. Цели, как правило, сводились к следующему:<br>
  <br>
  - восстановление дооккупационных порядков (польская АК, албанский «Легалитет») 
  либо без изменений, либо с некоторыми поправками (сербские Четники, греческая 
  ЭДЭС);<br>
  <br>
  - полное изменение дооккупационных порядков, но без изменения общественно-политических 
  отношений (албанский «Национальный фронт»);<br>
  <br>
  - полное изменение дооккупационных порядков с полным изменением общественно-политических 
  отношений (все прибалтийские, белорусские и украинские партизанские организации).<br>
  <br>
  Далее определялась стратегия и тактика того или иного движения, осуществлять 
  которые полагалось определенным организационным структурам, наполненным людскими 
  ресурсами. Заметим, что некоммунистическим партизанам не удалось превзойти своих 
  коллег из коммунистического лагеря ни качественно (организационно), ни количественно. 
  Из всех перечисленных формирований только АК, Четники и УПА имели более или 
  менее организованные руководящие органы, боевые и тыловые части и соединения, 
  которые по каким-то параметрам могли сравниться с коммунистическим партизанским 
  движением. В то же время их численность уступала обычно на порядок. Так, например, 
  число советских партизан составляло примерно 1 млн., югославских — 800 тыс. 
  (для сравнения, Четников в лучшие времена насчитывалось около 50 тыс. человек, 
  а всех антикоммунистических партизан на территории СССР — не более 200 тыс.) 
  [8].<br>
  <br>
  <img src="a-nacpart11.jpg" width="198" height="274"><br>
  <br>
  Некоммунистические партизаны, как мы уже выяснили, действовали в определенных 
  исторических условиях и находились под воздействием целого ряда факторов, которые 
  и привели их в результате к гражданскому противостоянию, а некоторых — и к сотрудничеству 
  с оккупантами. На наш взгляд, такими факторами, определившими во многом динамику 
  развития этих движений, являются следующие: отношения с коммунистическим движением 
  Сопротивления, отношения с местным населением, отношения с коллаборационистами 
  и отношения с оккупантами.<br>
  <br>
  Советская историография однозначно утверждала, что некоммунистические партизанские 
  формирования были изначально настроены враждебно по отношению к коммунистам. 
  Как и большинство подобных утверждений, это является ошибочным. На самом же 
  деле их взаимоотношения претерпели существенную эволюцию — от попыток сотрудничества 
  до открытого противостояния. Иногда и вовсе складывалась ситуация, когда сотрудничество 
  перемежалось с противостоянием. Так, например, было в Греции, где коммунистическая 
  ЭЛАС и республиканская ЭДЭС дважды на протяжении 1942—1944 годов мирились и 
  начинали друг против друга войну. Или в Албании. Также пытались сотрудничать 
  друг с другом Четники и партизаны Иосипа Броз Тито. Однако, начиная с конца 
  1941 года, такие контакты прекратились и стороны перешли сначала к неустойчивому 
  нейтралитету, а потом к открытой вражде. Наиболее позитивный пример такого сотрудничества 
  показали польская АК и ее коллеги из коммунистической Армии Людовой (АЛ). История 
  не сохранила фактов их открытой вражды, тогда как всем известен апогей их совместной 
  борьбы: участие в Варшавском восстании 1944 года. Что касается совместной деятельности 
  советских и националистических партизан на территории СССР, то нам о ней ничего 
  не известно. В лучшем случае они придерживались нейтралитета, как в Белоруссии, 
  в худшем — сражались больше друг с другом, чем с оккупантами (Украина) [9]. 
  <br>
  <br>
  <img src="a-nacpart12.jpg" width="198" height="146"><br>
  <br>
  Начиная примерно с конца 1941—начала 1942 года, между коммунистами и некоммунистами 
  наметился разрыв, который впоследствии перешел в открытое гражданское противостояние. 
  Каковы причины этого разрыва? Для каждой страны они индивидуальны. Однако среди 
  основных назовем следующие:<br>
  <br>
  - политико-идеологические. Курс коммунистов на полную смену общественного строя 
  и социальные преобразования, иногда в ущерб борьбе с оккупантами, не мог привести 
  в восторг даже идейно близких им социалистов, не говоря уже о правых республиканцах 
  и монархистах. Такие причины были решающими. Например, для Югославии, Греции 
  и Албании. Что же касается СССР, побудительным мотивом для прибалтийских, белорусских 
  и украинских партизан был именно антикоммунизм;<br>
  <br>
  - национальные. Этот комплекс причин был характерен для таких многонациональных 
  государств, как Югославия и СССР, и отчасти Польша, так как АК действовала не 
  только на собственно польской территории, но и на землях Южной Литвы, Западной 
  Белоруссии и Западной Украины. Коммунисты, как правило, придерживались интернациональной 
  идеологии, а Четники, члены АК, прибалты, белорусы и украинцы были соответственно 
  националистами. Тем более, что, например, в СССР многие действовавшие в Прибалтике, 
  Белоруссии и Украине партизанские отряды состояли из этнических русских или 
  выходцев из восточных земель этих республик. Националистами же были преимущественно 
  «западенцы»;<br>
  <br>
  - религиозные. Они отчасти являются продолжением предыдущих, так как фактор 
  религиозной принадлежности играл значительную роль в самоидентичности многих 
  национальных групп (особенно в Югославии, Албании, Польше и западных регионах 
  СССР). И дело тут даже не в том, что коммунисты придерживались атеистической 
  идеологии. Иногда религиозная вражда лежала больше в исторической, чем идеологической 
  плоскости. Например, сербские Четники были православными, а в армии Тито было 
  много боснийских мусульман и католиков-хорватов. В Албании же каждое из указанных 
  партизанских формирований представляло определенную религиозную группу — католиков 
  («Национальный фронт») или мусульман («Легалитет»);<br>
  <br>
  - военные. Эти причины были определяющими обычно там, где коммунистические партизаны 
  пытались проникнуть на территорию, которую националисты считали своей (так, 
  например, произошло в Западной Белоруссии и Западной Украине) [10].
  <br>
  <br>
  <img src="a-nacpart13.jpg" width="198" height="131"><br>
  <br>
  Некоммунистические течения из движения Сопротивления не располагали такими возможностями, 
  как их коммунистические коллеги (например, в СССР). Поэтому и масштабы их были 
  менее значительными. И все-таки подчеркнем: внешняя помощь, разумеется, играет 
  не последнюю, но и не самую главную роль. В данном случае гораздо важнее поддержка 
  местного населения, в среде которого находятся партизаны. Помощь извне не всегда 
  может прийти вовремя. Так, к слову, и случилось с советскими партизанами, когда 
  в 1941—1942 годах государству было явно не до них. Местное же население для 
  партизан является и источником снабжения продовольствием, и резервом людских 
  ресурсов, и в какой-то степени «окном» во внешний мир. Но все это так, когда 
  население настроено дружественно (или хотя бы нейтрально) по отношению к партизанам. 
  В противном случае их отряды обречены на бездействие, уничтожение или уход из 
  этой местности.<br>
  <br>
  Советская научная и художественная литература утверждала, что население на оккупированных 
  территориях поддерживало только коммунистов. Тогда как можно объяснить тот факт, 
  что многие некоммунистические формирования существовали до конца оккупации, 
  а на некоторых территориях действовали и гораздо дольше? Теперь не секрет, что 
  не везде население относилось, например, к советским партизанам лояльно или 
  даже нейтрально. Известны случаи откровенной вражды. Например, на присоединенных 
  в 1939—1940 годах территориях (Прибалтика, Западная Белоруссия и Западная Украина) 
  или на территориях, где нерусское население преобладало, либо было равным по 
  численности русскому (Северный Кавказ). Именно здесь коллаборационизм принял 
  свои наиболее крайние формы, а некоммунистическое партизанское движение было 
  гораздо мощнее просоветского. Не иначе, как отказом населения поддерживать коммунистов, 
  можно объяснить массовые депортации из Прибалтики и Западной Украины в 1945—1946 
  годах. Ясно, что затевались они с целью подорвать базу националистического подполья 
  и лишить его материальных и людских ресурсов [11].<br>
  <br>
  <img src="a-nacpart14.jpg" width="198" height="302"><br>
  <br>
  Коммунисты во время войны и коммунистические историки в послевоенное время часто 
  обвиняли представителей других партизанских движений в том, что они практически 
  не принимали участия в борьбе с оккупантами. Отчасти это правда, и связана эта 
  тактика прежде всего с отношением к местному населению. Например, Четники, албанский 
  «Национальный фронт» и АК начиная с 1942 года не проводили масштабных операций 
  против оккупантов из-за боязни репрессий последних против мирного населения. 
  Например, в Сербии немцы за каждого убитого солдата расстреливали 100 человек, 
  а за каждого раненого — 50. Поэтому лидер Четников Дража Михайлович, чтобы оправдать 
  свою тактику, даже ввел специальный термин «сбережение населения». С другой 
  стороны, такую тактику нельзя объяснять только заботой о населении. Многие партизанские 
  руководители некоммунистических формирований берегли свои силы для последующей 
  после ухода немцев борьбы за власть [12]. <br>
  <br>
  <img src="a-nacpart15.jpg" width="198" height="131"><br>
  <br>
  К сожалению, коммунисты боролись с оккупантами, не считаясь с репрессиями против 
  мирных жителей. Тем не менее, симпатии к коммунистам также являются фактом. 
  Например, в Греции население с 1941 по 1944 год устойчиво поддерживало их практически 
  по всей территории страны, а республиканцы и монархисты так и не вышли за пределы 
  Эпира (северо-западная Греция). В Югославии партизан Тито с большой охотой поддерживали 
  в Боснии, так как местное мусульманское население очень боялось репрессий со 
  стороны Четников. Аналогичная ситуация сложилась и в Западной Белоруссии, где 
  польская АК уничтожала белорусское население наравне с немцами [13].<br>
  <br>
  Неоднозначным для понимания является вопрос взаимоотношений некоммунистического 
  движения Сопротивления и коллаборационистов. И здесь нельзя отчасти не согласиться 
  с советскими историками, которые считали их единым целым. Не будет преувеличением 
  сказать, что они не боролись друг с другом, а мирно сосуществовали. <br>
  <br>
  <img src="a-nacpart16.jpg" width="198" height="284"><br>
  <br>
  Одной из причин такой политики было то, что коллаборационизм как явление не 
  был однородным. С немцами могли сотрудничать как явные безыдейные прислужники, 
  так и те, кто видел в таком сотрудничестве пользу для своего народа (национальное 
  освобождение, обретение государственности и т.п. ). Многие из них всерьез надеялись, 
  что немцы со временем предоставят им полную свободу действий и передадут им 
  рычаги управления государством. Другие рассчитывали на то, что, «врастая» таким 
  образом в оккупационный аппарат, придадут ему национальный характер со всеми 
  вытекающими последствиями. На такой поворот событий очень надеялись, например, 
  украинские и белорусские националисты. И как это ни парадоксально, но это был 
  один из вариантов борьбы за что-либо. А некоммунистическое движение Сопротивления 
  — это второй вариант. Другое дело, что к 1943 году даже самые ярые националисты 
  и антикоммунисты поняли, что никакой независимости от немцев они не получат. 
  В лучшем случае это может быть протекторат по образцу Богемии и Моравии, но 
  не больше. И именно 1943 год стал рубежным во взглядах многих коллаборационистов: 
  часть из них переходит к коммунистам, а часть — в националистическое движение 
  Сопротивления. Не секрет, что, например, УПА и ЛОА свои наиболее квалифицированные 
  кадры получили из местной полиции, которая целыми подразделениями стала переходить 
  на их сторону. В Греции же ситуация развивалась в обратном направлении. Когда 
  оккупанты предоставили греческому правительству возможность вести более или 
  менее самостоятельную политику (декабрь 1942 года), часть членов ЭДЭС влилась 
  в созданные этим правительством «охранные батальоны» [14]. <br>
  <br>
  <img src="a-nacpart17.jpg" width="198" height="279"><br>
  <br>
  Из сказанного видно, что коллаборационисты и партизаны одной национальности 
  не только не боролись между собой, но даже поддерживали друг друга. По сути, 
  это были «сообщающиеся сосуды», что впоследствии и побудило коммунистов обвинять 
  их в сотрудничестве с оккупантами. В случае, когда на одной территории действовали 
  коллаборационисты и партизаны разных национальностей или вероисповеданий, за 
  редким исключением, имело место непримиримое противостояние. Если же эта борьба 
  определялась не только идеологическими и социальными, но еще и религиозно-национальными 
  и историческими противоречиями, то она выливалась в чудовищную резню, как, например, 
  в Югославии и Украине. Сегодня всем известны события, которые происходили на 
  Волыни в 1943—1944 годах, когда по приказу своего руководства УПА развернула 
  политику деполонизации этого края. Поляки отвечали им тем же. Однако справедливости 
  ради следует сказать, что АК вела себя не лучше. Так, на ее совести тысячи убитых 
  белорусов, украинцев и литовцев, которые не хотели жить в Польше образца 1939 
  года. Естественно, что в такой борьбе страдало прежде всего мирное население 
  [15].<br>
  <br>
  Главным обвинением против некоммунистических течений движения Сопротивления 
  было то, что они сотрудничали с оккупантами, или то, что оккупанты принимали 
  участие в создании этих движений, их вооружении и снаряжении. Приводятся разные 
  доказательства, вплоть до довоенного сотрудничества, например, ОУН с германской 
  разведкой. Такие факты имели место. Однако этот вопрос намного сложнее, чем 
  может показаться. Трудно не согласиться, что и сербские четники, и греческие 
  партизаны-роялисты, и украинские и белорусские националисты в основной своей 
  массе считали нацистов врагами. Но еще большими своими врагами они с определенного 
  момента стали считать коммунистов. Поэтому логика развития этих движений неминуемо 
  толкала их к союзу (пусть даже временному и тактическому) с оккупантами, которые 
  на тот момент были главной антикоммунистической силой.<br>
  <br>
  Формы сотрудничества с оккупантами существовали различные, как и причины, подтолкнувшие 
  партизан к этому сотрудничеству. Выше уже говорилось об одной из них: полном 
  или частичном нейтралитете. Так поначалу поступали Четники, АК и ЭДЭС. Однако 
  в той войне редко кому удавалось сохранять нейтралитет и быть «третьей силой 
  между нацизмом и коммунизмом». АК, например, выбрала путь сотрудничества с коммунистами. 
  Совместно с советскими партизанами и Красной армией поляки участвовали в освобождении 
  Львова и Вильнюса. Четники, ЭДЭС и албанский «Национальный фронт» полностью 
  перешли на сторону оккупантов и с 1943 года действовали как обычные коллаборационистские 
  формирования. Прибалтийских же, белорусских и украинских националистов немцы 
  начали активно снабжать вооружением и снаряжением, а также готовить для них 
  кадры. Все это, безусловно, продлило жизнь этим формированиям, но не настолько, 
  чтобы они так долго держались без поддержки местного населения: и ЛОА, и БОА, 
  и УПА продолжали сражаться и после ухода немцев, вплоть до 50-х годов прошлого 
  века [16].<br>
  <br>
  При подготовке данного материала автор не ставил перед собой цели написать историю 
  некоммунистического партизанского движения, а только попытался проанализировать 
  его как явление с политической, идеологической, национальной и военной точки 
  зрения. Тема эта весьма обширна, однако из уже приведенных выше фактов видно, 
  как мало мы о ней знаем. А если и знаем, то знания эти зачастую ошибочны. Выше 
  было сказано о двух методологических ошибках, допускаемых большинством тех, 
  кто пытается изучать эту тему. Несомненно, некоммунистическое движение Сопротивления 
  и коллаборационизм связаны между собой. Но связь эта сродни двум сторонам одной 
  медали, и смешивать эти понятия нельзя. Кроме того, как мы наглядно убедились, 
  такие формирования, как УПА, в той или иной форме существовали во всех оккупированных 
  немцами государствах. И уникального в этом ничего нет. С другой стороны, не 
  во всех странах естественная конкуренция между двумя течениями движения Сопротивления 
  (которая, к сожалению, существовала даже в условиях борьбы за национальное освобождение) 
  переросла в гражданское противостояние, продолжавшееся иногда и после войны 
  (Греция, Прибалтика, Украина). И виноваты в этом не только и не столько оккупанты, 
  коммунисты или националисты, а те особенности национального менталитета, которые 
  веками складывались в Восточной и Юго-Восточной Европе.<br>
  <br>
  Мы, граждане бывшего СССР, привыкли считать, что гражданская война может возникнуть 
  только из социально-экономических причин. Вся же мировая история учит нас, что 
  такие войны возникают только тогда, когда происходит столкновение базовых ценностей, 
  лежащих в основе самосознания той или иной группы населения. Таким столкновением 
  ценностей, более того, взглядов на будущее своего народа, и была война на территории 
  Украины, основные события которой развернулись с 1942 по 1954 год. А то, что 
  первый период этой войны совпал с немецкой оккупацией, только усложнило и без 
  того запутанный клубок противоречий, плоды которых мы пожинаем и по сей день.<br>
