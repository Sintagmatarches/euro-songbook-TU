  Фантазии относительно Готенланда так и остались фантазиями, а вот планы относительно 
  переселения немцев в Крым разные инстанции Третьего рейха неоднократно подавали 
  Гитлеру для рассмотрения. Откуда же предполагалось взять такое количество населения, 
  чтобы восполнить демографические потери, к которым бы привела «зачистка» полуострова 
  от всех «ненемцев»?<br>
  <br>
  Во-первых, руководство СС предлагало переселить в Крым 140 тыс. этнических немцев 
  из так называемой Транснистрии — территории СССР между реками Днестр и Днепр, 
  которая находилась под управлением Румынии. Этот план стоял на повестке дня 
  до самого освобождения Крыма, но немцы к нему так и не подступились [13].<br>
  <br>
  Во-вторых, генеральный комиссар «Таврии» Альфред Фрауэнфельд летом 1942 года 
  подготовил специальный меморандум (его текст, к сожалению, не сохранился), где 
  предлагал переселить в Крым жителей Южного Тироля, чтобы раз и навсегда решить 
  итало-германский спор по поводу этой территории. Известно, что Гитлер отнесся 
  к этой идее с большим энтузиазмом. Так, на одном из совещаний он сказал буквально 
  следующее: «Я думаю, что это великолепная идея. Кроме того, я также считаю, 
  что Крым и климатически, и географически подходит тирольцам, а по сравнению 
  с их родиной, он действительно земля, где текут реки с молоком и медом. Их переселение 
  в Крым не вызвало бы ни физических, ни психологических трудностей» [14].<br>
  <br>
  К слову, рейхсфюрер СС и Шеф германской полиции Генрих Гиммлер, в чьем ведении 
  находились все вопросы по «укреплению германской расы», также был не против 
  подобного решения «тирольской проблемы». Более того, он был готов уступить тирольцев 
  Фрауэнфельду, даже несмотря на то, что ранее планировал поселить их в «Бургундии» 
  — еще одном государстве, где после окончания войны должна была «концентрироваться 
  германская кровь». Но, по мнению Гиммлера, переселять их следовало только после 
  окончания войны [15]. В конце концов, Гитлер согласился именно с рейхсфюрером, 
  хотя и подписал в начале июля директиву, согласно которой выселение русских 
  из Крыма должно начаться уже сейчас, а украинцев и татар — чуть позже. <br>
  <br>
  <img src="a-otnemgibr4.jpg" width="198" height="220"><br>
  <br>
  В-третьих, во второй половине 1942 года Фрауэнфельд разработал еще один план. 
  На этот раз он предлагал переселить в Крым 2 тыс. немцев из Палестины. Как это 
  можно осуществить в условиях британ¬ской оккупации — оставалось «за скобками». 
  В целом же фантастичность этого плана была настолько очевидной, что даже Гиммлер 
  приказал отложить его до лучших времен [16].<br>
  <br>
  Конец всем планам по переселению положили протесты тех органов Вермахта, которые 
  отвечали за военную экономику. Так, в середине августа 1943 года начальник Верховного 
  командования Вермахта (ОКВ) генерал-фельдмаршал Вильгельм Кейтель решительно 
  выступил против каких-либо перемещений населения в условиях войны. Не без основания 
  он заметил, что «эвакуация» русских и украинцев — 4/5 всего населения Крыма 
  — полностью парализует экономическую жизнь. Тремя неделями позднее было решено, 
  что любые перемещения возможны только по окончании войны. С этим согласился 
  и Гиммлер, полагавший, что переселение немцев необходимо и планировать, и осуществлять, 
  но в условиях военной ситуации делать это крайне преждевременно. Заметим, что 
  Гиммлер самым решительным образом воспротивился планам по выселению татар из 
  Крыма. Правда, только в военный период. По его словам, это могло стать катастрофической 
  ошибкой. «Мы должны сохранить в Крыму хотя бы часть населения, которое смотрит 
  в нашу сторону и верит в нас», — подчеркивал рейхсфюрер [17].<br>
  <br>
  На этом можно было бы поставить точку, так как осенью 1943 года немцам стало 
  не до администрирования и не до национального вопроса: Крым был отрезан частями 
  Красной армии и превратился в «осажденную крепость». Но, как показали дальнейшие 
  события, это был не конец. Только теперь инициатива в подобном планировании 
  перешла от высшего нацистского военно-политического руководства к его представителям 
  на местах.<br>
  <br>
  Невероятно, но факт — в начале 1944 года немцы предприняли запоздалую попытку 
  свести воедино все свои усилия по политической и национальной «организации» 
  Крымского полуострова. В январе этого года командующий войсками Вермахта в Крыму 
  генерал-полковник Эрвин Йенеке приказал начать подготовку по созданию на полуострове 
  местного правительства (Landesregierung). По замыслу немецкого генерала оно 
  должно состоять из представителей трех основных национальностей, населяющих 
  Крым: татар, русских и украинцев (именно в таком порядке они перечислены в отчете 
  Йенеке командованию группы армий «А»). Основой этого правительства должны были 
  послужить органы местного самоуправления и соответствующие национальные организации 
  — мусульманские и украинские комитеты. В его компетенцию, при общем надзоре 
  со стороны немецкой военной администрации, планировалось передать:<br>
  <br>
  • административное руководство органами гражданской власти, а также командование 
  частями вспомогательной полиции;<br>
  <br>
  • все вопросы, касающиеся религии и просвещения;<br>
  <br>
  • благотворительность;<br>
  <br>
  • судопроизводство.<br>
  <br>
  На что в условиях полного окружения полуострова надеялись немцы, понять трудно. 
  Скорее, это был очередной пропагандистский шаг, который следует воспринимать 
  не более чем курьез. Тем не менее к марту 1944 года вся местная администрация 
  была в основном переформирована согласно этому плану. Но и этим замыслам немецкого 
  командования, естественно, не суждено было исполниться, так как уже в апреле 
  — мае 1944 года Крым был полностью освобожден частями Красной армии [18].<br>
