  Ситуация не менялась на полуострове до мая 1944 года. Чего не скажешь, например, 
  о немецких планах по решению национального вопроса.<br>
  <br>
  Выше уже шла речь о планах Альфреда Розенберга по разделу СССР. Как известно, 
  один из пунктов этого плана назывался «Украина с Крымом». Последующий меморандум 
  также подразумевал, что Крым будет частью будущей «Великой Украины» под названием 
  «Таврия». Однако многочисленные рукописные пометки на этом документе свидетельствуют 
  о том, что написание именно этого пункта далось Розенбергу с большим трудом. 
  Он явно понимал, что Крым только с большой натяжкой можно отнести к Украине, 
  так как число проживавших там украинцев было ничтожно мало (чтобы как-то решить 
  эту проблему, Розенберг предлагал выселить с полуострова всех русских, евреев 
  и татар). Но это был не единственный парадокс плана рейхсляйтера. Одновременно 
  он настаивал, чтобы Крым находился под прямым контролем правительства Третьего 
  рейха. Объясняя этот казус, Розенберг всячески подчеркивал «германское влияние» 
  на полуострове. Так, он утверждал, что до Первой мировой войны немецким колонистам 
  здесь принадлежали значительные территории. А еще в XVI столетии в Крыму жили 
  готы — одно из древних германских племен. Таким образом, «Таврию» только «технически» 
  можно присоединить к Украине. Управлять же ею должны из Берлина [9].<br>
  <br>
  Несмотря на свою явную противоречивость, планы Розенберга относительно Крыма 
  являлись только отражением «двойственной аргументации Гитлера о причинах его 
  германизации». Во-первых, по расчетам фюрера, Крым должен стать «немецким Гибралтаром», 
  который позволит контролировать все Черное море. Во-вторых, планировалось превращение 
  полуострова в «один огромный немецкий курорт» (о чем мечтал шеф Германского 
  трудового фронта Роберт Лей). Как отмечал американский исследователь Александр 
  Даллин, «в этих завоевательных планах реальность и фантазия смешались поровну» 
  [10]. <br>
  <br>
  <img src="a-otnemgibr3.jpg" width="198" height="240"><br>
  <br>
  Более конкретно о судьбе Крыма Гитлер высказался на совещании военно-политического 
  руководства Третьего рейха 16 июля 1941 года. В своей речи он выделил его из 
  ряда других оккупированных советских территорий и подчеркнул, что полуостров 
  «необходимо очистить от всех чужаков и заселить германцами». В частности, русских 
  предполагалось выселить в Россию. По воспоминаниям одного из присутствующих, 
  фюрер выразился так: «Она для этого достаточно велика» [11].<br>
  <br>
  Как свидетельствуют документы, «крымский вопрос» и судьба населения полуострова 
  занимали Гитлера и в последующие месяцы. Когда Розенберг в очередной раз посетил 
  его в декабре 1941 года, фюрер повторил, что «Крым должен быть полностью очищен 
  от негерманского населения». Эта встреча интересна еще с той точки зрения, что 
  на ней затрагивалась проблема так называемого «готского наследия» в истории 
  полуострова. Имея в виду его значимость, Гитлер выразил желание, чтобы после 
  окончания войны и решения вопроса с населением Крым получил название Готенланд. 
  Розенберг сказал, что уже думает над этим, и предложил переименовать Симферополь 
  в Готенбург, а Севастополь — в Теодорихсхафен. Продолжением «готских планов» 
  Гитлера и Розенберга явилась археологическая экспедиция, организованная генеральным 
  комиссаром «Таврии» Фрауэнфельдом в июле 1942 года. Руководителем этого мероприятия 
  был назначен фюрер СС и полиции «Таврия» Людольф фон Альвенслебен*. В ходе экспедиции 
  было обследовано городище Мангуп — бывшая столица княжества Феодоро, которое 
  было уничтожено турками в 1475 году. ”Генрих Вывод нацистских археологов: крепость 
  Мангуп, а также целый ряд городов на Южном берегу Крыма были построены готами. 
  Этот и другие тезисы изложены в книге «Готы в Крыму», автором которой является 
  один из участников экспедиции, полковник Вернер Баумельбург [12].<br>
