  <br>
  Согласно планам министерства Розенберга Крым вместе с Херсонской и Запорожской 
  областями должен был войти в генеральный округ «Таврия» (Generalbezirk Taurien), 
  с общей площадью 22 900 км и населением 661 981 человек (по состоянию на 1 сентября 
  1941 года). Центром округа выбран Мелитополь. Генеральный округ «Таврия» являлся 
  составной частью рейхскомиссариата «Украина» (Reichskomissariat Ukraine). Помимо 
  него в эту административную единицу включались генеральные округа «Волыния-Подолия», 
  «Житомир», «Киев», «Николаев» и «Днепропетровск» [6].<br>
  <br>
  Высшим органом гражданской оккупационной администрации в генеральном округе 
  «Таврия» должен был стать генеральный комиссариат, который поручалось возглавить 
  Альфреду Фрауэнфельду*. Но поскольку до лета 1942 года подведомственная ему 
  территория являлась ближним тылом действующей армии, к своим обязанностям он 
  смог приступить только 1 сентября 1942 года. К этой дате относится окончательное 
  оформление территории генерального округа «Таврия», каким его видели в министерстве 
  Розенберга. Однако оставался один нюанс. Территория Крыма так и не перешла под 
  юрисдикцию генерального комиссара Фрауэнфельда. По указанным выше причинам полуостров 
  находился под двойным управлением: гражданским (номинально) и военным (фактически). 
  То есть из состава генерального округа Крым не изымался, и гражданские чиновники 
  также никакой власти здесь не имели. Реальная власть на полуострове принадлежала 
  командующему расквартированных здесь частей Вермахта [7].<br>
  <br>
