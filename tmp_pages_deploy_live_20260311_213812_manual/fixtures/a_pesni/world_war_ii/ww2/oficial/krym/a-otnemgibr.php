	 <strong>Олег Романько, кандидат исторических наук, доцент кафедры украиноведения Крымского 
  государственного медуниверситета им. С.И. Георгиевского (Симферополь)</strong><br>
  <br>
<span class="header"><strong>ОТ «НЕМЕЦКОГО ГИБРАЛТАРА» ДО АРИЙСКОГО «ГОТЕНЛАНДА». 
  КРЫМСКИЙ ПОЛУОСТРОВ В МАЛОИЗВЕСТНЫХ ПЛАНАХ ВОЕННО-ПОЛИТИЧЕСКОГО РУКОВОДСТВА 
  ТРЕТЬЕГО РЕЙХА (1941—1944) <br>
  </strong><br></span>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=97&par=74&l=&art=494" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, №14, 2006</em></a><em>.</em><br>
  <br>
  <br>
  Как известно, Крымский полуостров занимает важное геополитическое и стратегическое 
  положение, благодаря которому с древнейших времен он является центром средоточения 
  и местом контакта многих цивилизаций Востока и Запада. Несомненно, это положительный 
  момент. В то же время геостратегическое достоинство Крыма — это и его беда. 
  С самых первых письменных поминаний лейтмотивом всей политической и общественной 
  жизни полуострова была борьба «великих держав всех времен и народов» за его 
  территорию. Кто только не претендовал на него: Понтийское царство и Римская 
  империя, Византия и Хазарский каганат, Золотая Орда и Османская Турция. Наконец, 
  в 1783 году Крым стал частью территории Российской империи. Но, даже пребывая 
  в ее составе (а с 1920 года — в составе СССР), Крымский полуостров не переставал 
  привлекать взгляды завоевателей: в первой половине XX века на него очень активно 
  стала претендовать Германская империя.<br>
  <br>
  Гитлеровская Германия являлась, по сути, наследницей кайзеровского государства. 
  И ее восточная политика — лишнее тому подтверждение: не секрет, что и до и после 
  прихода к власти нацисты строили многочисленные планы политической и административной 
  «организации восточного пространства». После начала войны с СССР ее лидеры получили 
  возможность осуществлять свои проекты. Фактически они занимались этим до начала 
  1945 года.<br>
  <br>
  Проекты всесторонней «организации восточного пространства» разрабатывали все 
  ведомства Третьего рейха, в той или иной степени имели отношение к его внешней 
  и национальной политике. С большим или меньшим успехом и с оглядкой на Гитлера, 
  который был для них верховным арбитром, это делало партийное руководство, армия, 
  СС. Но будущее устройство гражданского управления на оккупированных территориях 
  СССР вызывало наибольшее количество споров среди нацистского военно-политического 
  руководства. Дело в том, что, если военное управление могло носить только временный 
  характер, а аппарат СС не имел права вмешиваться в вопросы администрирования, 
  ограничиваясь выполнением исключительно карательных функций, гражданская администрация, 
  напротив, должна была стать переходной формой на пути к будущему политическому 
  устройству всего «восточного пространства». Каким оно будет после победы Германии? 
  На этот вопрос следовало ответить заранее, и с как можно большей политической 
  ясностью, чтобы не оттолкнуть возможных союзников.<br>
  <br>
  Планы по «организации» имелись почти для всех частей «восточного пространства». 
  Какие-то из них были более удачными, некоторые не годились вовсе. Что же касается 
  Крыма, то нацисты, при всей важности этого региона, так окончательно и не решили 
  его судьбу. Станет полуостров частью «вассальной Украины» или это будет территория, 
  напрямую управляемая из Рейха? На эти вопросы различные немецкие инстанции пытались 
  ответить до конца 1943 года. После же того, как Крым был отрезан частями Красной 
  армии, проблема его политического устройства стала неактуальной.<br>
  <br>
  Тем не менее административные планы были только одной из сторон будущей «организации» 
  Крыма. Не секрет, что главной особенностью общественно-политической ситуации 
  на полуострове во все времена была его многонациональность. И поэтому, какие 
  бы планы нацисты ни строили, в своих «выкладках» по Крыму они не могли игнорировать 
  межнациональные отношения. Что делать с населявшими Крым многочисленными национальными 
  группами? Приходится признать, что при всем радикализме гитлеровской концепции 
  национальной политики решение этого вопроса осталось на уровне теорий.<br>
  <br>
  Итак, по порядку. Цели Германии в войне против СССР были окончательно сформулированы 
  30 марта 1941 года на совещании Гитлера с германским военно-политическим руководством. 
  В военном отношении они заключались в достижении линии «Архангельск — Астрахань», 
  а в политической плоскости необходимо было сделать так, чтобы «никакая организованная 
  сила не могла противостоять немцам по эту сторону Урала». В заключение своего 
  выступления Гитлер выразился более конкретно: «Наши задачи в отношении России 
  — разгромить ее вооруженные силы, уничтожить государство». Для управления же 
  захваченными советскими территориями фюрер предлагал создать «протектораты»: 
  в Прибалтике, на Украине и в Белоруссии [1]. Скорее, речь шла только о политической 
  ширме, а не о протекторатах, таких как в Чехии и Моравии.<br>
  <br>
  Мартовское совещание знаменательно еще и тем, что на нем все вопросы будущего 
  административно-политического планирования на «восточных территориях» были переданы 
  в ведение рейхсляйтера Альфреда Розенберга* — главного нацистского теоретика, 
  знатока внешнеполитических и межнациональных отношений. Уже 2 апреля 1941 года 
  Розенберг представил первый меморандум, в котором полностью отразил свои политические 
  взгляды и концепцию решения национального вопроса в СССР. Последний он предлагал 
  разделить на семь регионов:<br>
  <br>
  <hr>
  <em>* Розенберг (Rosenberg), Альфред (12.01.1893 — 16.10.1946), немецкий партийный 
  и государственный деятель, публицист, теоретик. Из прибалтийских немцев. До 
  1918 г. проживал в России, где и получил образование. В 1918 г. переехал в Германию. 
  С 1919 г. член эзотерического общества «Туле». Позднее вступил в Немецкую рабочую 
  партию. В 1921 г. вступил в Штурмовые отряды (СА). С марта 1923 по декабрь 1937 
  г. главный редактор нацистской газеты «Фелькишер Беобахтер». С 1930 по 1945 
  г. депутат Рейхстага. С апреля 1933 по май 1945 г. занимал пост руководителя 
  Внешнеполитического ведомства Нацистской партии. Рейхсляйтер. С 1934 г. отвечал 
  за контроль над духовным и научным образованием и воспитанием членов нацистской 
  партии. Негласный «начальник по идеологическим вопросам» и «главный теоретик 
  нацистской партии». Автор книги «Миф ХХ века», в которой он изложил свой взгляд 
  на мировую историю и основные постулаты национал-социалистической идеологии. 
  С 17.06.1941 по 30.04.1945 г. имперский министр по делам оккупированных восточных 
  областей. В мае 1945 г. арестован американской военной полицией. На Нюрнбергском 
  процессе приговорен к смертной казни через повешение.</em><br>
  <hr>
  <br>
  • «Великороссия» с центром в Москве;<br>
  <br>
  • Белоруссия с Минском или Смоленском в качестве столицы;<br>
  <br>
  • «Балтенланд» (Эстония, Латвия и Литва);<br>
  <br>
  • Украина и Крым с центром в Киеве;<br>
  <br>
  • Донская область с Ростовом-на-Дону в качестве столицы;<br>
  <br>
  • Кавказский регион;<br>
  <br>
  • Туркестан (советская Центральная Азия).<br>
  <br>
  Согласно концепции, изложенной в этом документе, Россия (вернее, то, что от 
  нее оставалось) отрезалась от остального мира кольцом нерусских государств. 
  Кроме того, по замыслу Розенберга она теряла ряд территорий с русским населением. 
  Так, Смоленск отходил к Белоруссии, Курск, Воронеж и Крым — к Украине, а Ростов-на-Дону 
  и нижняя Волга — к Донской области. В будущей «Великороссии» предполагалось 
  «полностью уничтожить еврейско-большевистскую администрацию», а сама она «должна 
  была подвергнуться интенсивной экономической эксплуатации» со стороны Германии. 
  Помимо этого новое территориальное образование получало статус значительно более 
  низкий, чем у окружавших его «государств», и превращалось в «приемник для всех 
  нежелательных элементов с их территорий» [2].<br>
  <br>
  Этот план вызвал существенные замечания Гитлера, который полагал, что деление 
  будущей оккупированной территории не должно быть таким дробным, а новые административные 
  единицы — искусственными. Например, создание отдельной Донской области, на его 
  взгляд, не оправдано ни политически, ни экономически, ни даже с точки зрения 
  национальной политики. Это же касалось и Белоруссии. Фюрер считал, что она должна 
  быть объединена с Прибалтикой — что удобно с административной точки зрения. 
  И такие замечания высказывались практически по всем пунктам меморандума Розенберга. 
  Однако его генеральной линии они почти не затронули. Рейхсляйтер продолжал говорить 
  о «дальнейшей дифференциации среди населения оккупированных территорий», «украинском 
  народе и его свободе», «освобождении народов Кавказа» и «спасении эстонской, 
  латышской и литовской наций». Что же касается «России или русских территорий, 
  то о каких-либо изменениях в их судьбе не могло быть и речи» [3].<br>
  <br>
  20 июня 1941 года в Берлине состоялось совещание высшего военно-политического 
  руководства Германии, на котором Розенберг представил Гитлеру новый план будущего 
  административно-политического устройства того, что должно было остаться от СССР. 
  Предполагалось создать пять административных единиц — рейхскомиссариатов (Reichskomissariat):<br>
  <br>
  • «Московия» (центральные области России);<br>
  <br>
  • «Остланд» (Прибалтика и Белоруссия);<br>
  <br>
  • «Украина» (большая часть Украины и Крым);<br>
  <br>
  • «Кавказ» (Северный Кавказ, Закавказье и Калмыкия);<br>
  <br>
  • «Туркестан» (Средняя Азия, Казахстан, Поволжье и Башкирия).<br>
  <br>
  Эти единицы планировалось создавать по мере продвижения линии фронта на Восток 
  и после умиротворения указанных регионов [4].<br>
  <br>
  В целом Гитлер согласился с таким решением проблемы, и уже 17 июля 1941 года 
  подписал приказ о введении гражданского управления на оккупированных советских 
  территориях. Согласно этому приказу было создано Министерство оккупированных 
  восточных областей (Reichsministerium fьr die besetzen Ostgebiete) — главный 
  руководящий орган для указанных территориальных образований. Возглавил министерство 
  Альфред Розенберг. Однако вследствие провала «молниеносной войны» против СССР 
  удалось создать только два рейхскомиссариата — «Остланд» и «Украина». Они начали 
  функционировать 1 сентября 1941 года. Окончательно их территории оформились 
  только к декабрю 1941 года [5].<br>
  <br>
