  <br>
  Сегодня, спустя 60 лет, бывает довольно трудно проникнуть в логику принятия 
  решений на тех или иных уровнях военной и государственной власти. Это можно 
  сказать и о знаменитом заседании бюро обкома 18 ноября 1942 г., принявшем решение 
  «Об ошибках, допущенных в оценке поведения крымских татар по отношению к партизанам, 
  о мерах по ликвидации этих ошибок и усилению политической работы среди татарского 
  населения». Оно производит впечатление резкой перемены оценки «татарского фактора» 
  в крымских событиях. Ещё 16 июля бюро обкома рассматривает вопрос о партизанском 
  движении и принимает постановление, в котором трудно усмотреть даже ростки «новых 
  тенденций». В нём, в частности, в качестве одной из главных задач партизанских 
  отрядов Крыма определялось «установление связи с населенными пунктами, в особенности 
  татарскими, приобретение своих агентов, разведчиков и агитаторов…», а также 
  предписывалось «выявлять фашистских ставленников, предателей — истреблять их». 
