  <br>
  Советское командование также высоко оценивало деятельность партизан и командования 
  партизанскими силами, о чем свидетельствует письмо Военного совета Крымского 
  фронта, подписанное маршалом Буденным от 3 мая 1942 г., а также радиограмма 
  Мехлиса, направленная в лес 1 мая. В апреле 1942 г. Мокроусову было присвоено 
  звание полковника. Даже после отзыва, в начале июля 1942 г., из Крыма по приказу 
  командования Северо-Кавказским фронтом Мокроусову не предъявлялись какие-либо 
  обвинения (об обвинениях маршала Будённого в свой адрес он узнал только в декабре 
  1942 г.), так что оправдываться, тем более «сваливая ответственность на татар», 
  у него не было необходимости.<br>
  <br>
  Вообще из донесений Мокроусова в центр, равно как и из его приказов, никак не 
  вырисовывается образ татарофоба, который создают некоторые авторы, находящиеся 
  в плену собственных сомнительных концепций. Наверное, лучше всего отношение 
  к «татарскому вопросу» выражает записка Мокроусова, направленная в декабре 1941 
  г. И.Г. Генову, сообщавшая о трудностях установления контакта с татарским населением.<br>
  <br>
  «Боюсь, что у Вас, — писал Мокроусов, — сложилось неправильное понятие о настроении 
  татарского населения. Отсюда исходит пробел вашей работы, заключающейся в ненахождении 
  связи с татарскими отрядами[115] Алуштинского и Судакского районов. Прошу учесть 
  это, памятуя о том, что татарское население в подавляющей массе своей есть и 
  будет советским. А для того, чтобы ускорить активизацию татарского населения 
  в нашу пользу, военкому Попову нужно уделить максимум внимания на политработу 
  среди татарского населения, при этом вы должны беспощадно расправляться с бывшими 
  кулаками, муллами, разложившимися и особенно должны относиться осторожно к рядовой 
  массе даже при условии, если в среде таковой оказались элементы, которые временно 
  пошли за предателями родины, не осознав…»[116].<br>
  <br>
  Трудно сказать, в какой мере текст этой записки отражал действительное отношение 
  Мокроусова к сложившейся в крымскотатарской среде ситуации, однако он свидетельствует, 
  по крайней мере, о том, что, пусть из тактических соображений, но Мокроусов 
  препятствовал тому, чтобы у его непосредственного окружения создавался по отношению 
  к татарам «образ врага». Любопытно, что эта записка не фигурировала в последовавшем 
  через некоторое время расследовании обкомом отношения партизанского командования 
  к крымскотатарскому населению.<br>
  <br>
  Тем не менее деятельность Мокроусова не была лишена недостатков[117]. Он отличался 
  довольно тяжелым характером, был чрезвычайно обидчив, «скор на расправу» и вследствие 
  этого допустил возникновение серьёзного конфликта с представителями командного 
  состава попавших в партизанские отряды в 1941—42 гг. остатков воинских частей. 
  В этот конфликт вынужден был вмешиваться Крымский обком, но, что ещё более важно, 
  — командование Северо-Кавказского фронта во главе с маршалом Будённым, который 
  взял сторону военных. Именно этот конфликт и явился основной причиной обвинений 
  Мокроусова в том, что он не справился с руководством движения и в конце концов 
  привел его к отставке и последующей «опале». Мы не ставим целью подробно описывать 
  конфликт внутри партизанского руководства, однако он в известной степени повлиял 
  и на решение «татарского вопроса», поэтому мы также не можем не коснуться его.<br>
  <br>
  9<br>
  <br>
  Как уже было сказано, при отступлении 51-й и Приморской армии в партизанские 
  отряды влилось значительное количество военнослужащих регулярных частей, которые 
  в 1941—42 гг. составляли до трети боевого состава партизанских сил. В партизанских 
  отрядах оказались и представители высшего офицерства — в частности, почти в 
  полном составе штаб 48-й кавдивизии во главе с генерал-майором Д.С. Аверкиным. 
  Мокроусов, как опытный руководитель, предвидел подобную ситуацию и её последствия: 
  «предполагалось, — объяснял он позже в одном из отчетов, — что при отступлении 
  Красной Армии из Крыма не все части успеют переправиться на кавказское побережье 
  и оставшиеся в Крыму должны осесть в лесу. Поэтому еще при составлении схемы 
  выплыл вопрос о подчинённости и возможных недоразумениях на этой почве.., схемой 
  и нашим приказом предусматривалось подчинение всех начальников, попавших в лес 
  руководству партизан. Впоследствии в этом духе был отдан приказ по погранвойскам 
  и по 51-й армии»[118]. Однако с «недоразумениями», о которых говорил Мокроусов, 
  все же покончено не было. Прежде всего, многие военные не горели желанием оставаться 
  в отрядах, они видели своей главной задачей — пробиваться к Севастополю, а после 
  образования Крымского фронта — соединяться с его войсками. Кроме того, для офицеров 
  регулярных частей было непривычным и отчасти унизительным входить в подчинение 
  командованию отрядов, в большинстве состоявшему из гражданских людей. С другой 
  стороны, и партизаны не хотели принимать к себе «чужаков», которых необходимо 
  было кормить. На этой почве произошло несколько неприятных инцидентов, например, 
  когда партизаны отнимали у отступающих солдат оружие, имел место и весьма трагический 
  случай, когда разоруженная таким образом группа военнослужащих была разгромлена 
  румынами[119]. Всё это вскоре стало помехой для партизанского командования. 
  Нельзя сказать, что Мокроусов не пытался разряжать напряженные ситуации. Офицерам 
  предоставлялись командные должности, военные стали объединяться в самостоятельные 
  так называемые красноармейские отряды, но в полной мере это не решило проблему. 
  И дело здесь было не только в том, что командных должностей для кадровых офицеров 
  не хватало и у красноармейских отрядов не было собственных баз, но и в том, 
  что военные и «гражданские» партизаны зачастую придерживались разных взглядов 
  на тактику и характер партизанской борьбы, что в свою очередь нередко вызывало 
  трения между ними (речь идет о руководстве, а не о рядовых бойцах). Ситуация 
  усугублялась тем, что непосредственным руководством партизан Крыма являлось 
  командование Кавказского, затем Крымского и Северо-Кавказского фронтов и Приморской 
  армии в Севастополе, которому легче было найти общий язык с военными, чем с 
  Мокроусовым. В январе 1942 г. к партизанам перешли остатки разбитого Судакского 
  десанта во главе с майором Н.Г. Селиховым, назначенным по распоряжению командования 
  фронтом начальником 2-го района вместо И.Г. Генова. В феврале 1942 г. в расположение 
  этого района перешел и центральный штаб Мокроусова, с которым у Селихова сразу 
  же сложились весьма напряженные отношения. Постепенно дело дошло до конфликтной 
  ситуации, когда по распоряжению Мокроусова был арестован комиссар района Е.А. 
  Попов, которому Мокроусов угрожал расстрелом. Всё это в конце концов вызвало 
  вмешательство командования Северо-Кавказского фронта и лично маршала Будённого, 
  в результате чего Мокроусов и Мартынов в июле 1942 г. были отозваны на Большую 
  землю.<br>
  <br>
  Как уже было сказано, отношения Мокроусова с военными в определённой степени 
  повлияли и на решение обкомом «татарского вопроса», так как именно они заставляли 
  крымское руководство критически смотреть на всю деятельность командующего.<br>
  <br>
  Если сравнивать взгляды Мокроусова и военных на татарский вопрос, то последние 
  были более радикальными. Представители военного руководства были гораздо лучше 
  осведомлены о политике немцев по отношению к крымским татарам и не скрывали 
  того, что являются сторонниками самых крайних мер по отношению к «предателям»[120].<br>
  <br>
  Так, постоянный антагонист Мокроусова майор, затем полковник Селихов сообщал 
  в центр не только о том, что «немцы использовали татар как на Керченском, так 
  и на Севастопольском участках фронта» (Мокроусов сообщал лишь о такой возможности), 
  но и о работе в Крыму представителя партии «милли-фирка» Мустафы Садретдина, 
  «являющегося ближайшим соратником Джафера Сейдаметова», о чем штаб Мокроусова 
  не имел представления. («Этот Садретдин, — доносил Селихов, — имел директиву 
  съезда «миллифирка» о создании ханского Крыма, организации повстанческого движения 
  крымских татар»). И даже о том, что перед поездкой в Крым Садретдин посетил 
  Берлин, «где имел встречу с Гитлером и Гиммлером, от которых получил задание 
  о вооружении крымских татар против советской власти и создании добровольческих 
  отрядов по борьбе с партизанами»[121]. В то время как начштаба Мокроусова Сметанин 
  говорит о некоем «националистическом центре» в Крыму как о своей догадке, Селихов 
  сообщает достаточно подробные сведения о Симферопольском мусульманском съезде, 
  «на котором обсуждались вопросы вооружения татар, создания добровольческих отрядов 
  и уничтожение евреев»[122].<br>
  <br>
  Военные не только были лучше осведомлены о положении вещей в татарской среде, 
  но предлагали принять против «предателей» наиболее крутые меры. У кого и был 
  совершенно четкий взгляд без мокроусовских этно-геогафических подробностей относительно 
  того, что нужно делать с враждебными деревнями, так это у еще одного недруга 
  командующего — бригадного комиссара Попова. Строки его донесения от июня 1942 
  г. как будто предвосхищают печально известное постановление ГКО: «Крымские татары 
  в отечественной войне показали себя как предатели нашей Родины, которые начиная 
  с момента отступления частей Красной Армии целиком и полностью (выделено авт.) 
  перешли на сторону немецких захватчиков и борятся не только против партизан, 
  но также целыми соединениями участвуют на передовых позициях»[123]. Он же рекомендует 
  задействовать против опорных пунктов добровольческих отрядов авиацию. Наконец, 
  сменивший в июле 1942 г. Мокроусова полковник М.Т. Лобов недвусмысленно возлагает 
  ответственность за то, что «большая часть предгорного татарского населения взяла 
  оружие и действовала против партизан и Красной Армии» на Крымский Обком и местный 
  НКВД[124].<br>
  <br>
  Поскольку непосредственным начальством Селихова, Попова и др. представителей 
  оказавшихся в окружении военных был не Крымский обком ВКП(б), а командование 
  Крымского, затем Северо-Кавказского фронта, эти документы ложились в основном 
  на стол маршалу Буденному вместе с донесениями, в которых содержались многочисленные 
  претензии военных к Мокроусову по вопросам организации партизанской деятельности. 
  Буденный, как известно, сам в прошлом кавалерист, благоволил своим товарищам 
  по оружию. В результате у командующего фронтом формировалась своеобразная картина 
  положения дел в Крыму. Буденный, а вместе с ним и командование Северо-Кавказским 
  фронтом были убеждены в том, что в партизанских отрядах Крыма царит дезертирство, 
  процветает предательство и военная некомпетентность, что руководство в лице 
  Мокроусова со своей задачей не справилось и все партизанское движение держится 
  лишь на остатках воинских частей. Поскольку непосредственным создателем и руководителем 
  командования партизанскими отрядами Крыма был Крымский обком ВКП(б), этот партийный 
  орган начинал выглядеть в глазах высшего военного руководства и руководства 
  партизанским движением в Москве все в менее привлекательном свете. Получалось, 
  что обком не справился с ситуацией дважды — первый раз, не подняв на должный 
  уровень воспитание крымскотатарских масс, и особенно татар-коммунистов, и второй, 
  сформировав партизанское руководство из недостаточно компетентных в военном 
  отношении людей. Если вторая проблема была решена явочным порядком (Мокроусов 
  и Мартынов в июле 1942 года были отозваны, командующим назначен полковник Лобов), 
  то первая оставалась без ответа. Между тем именно она приобретала особую важность. 
  После падения Севастополя в Крыму было решено оставить 300—400 наиболее боеспособных 
  людей, которым надлежало продолжать борьбу в глубоком тылу врага. В связи с 
  этим вопрос о том, что делать с ситуацией крымскотатарского сотрудничества с 
  оккупантами и враждебного отношения их к партизанам, встал «ребром». Его решения 
  требовал Центр и, судя по всему очень настойчиво. <br>
