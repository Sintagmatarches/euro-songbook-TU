  <br>
  Уже одно это говорит о том, как оценивал Манштейн опасность со стороны партизан 
  в Крыму. Нередко для борьбы против партизан немцам приходилось отвлекать еще 
  более значительные силы, как это было, например, в ходе декабрьских боёв в лесах 
  госзаповедника, когда немцам так и не удалось уничтожить центральный штаб партизан, 
  а также во время так называемого большого прочёса в июле 1942 г., когда против 
  партизан были задействованы до 20 тыс. военнослужащих немецких и румынских частей, 
  а также крымскотатарские добровольцы. Характерно, что немцы почти вдвое преувеличивали 
  количество партизан, скрывавшихся в Крымских горах (Манштейн полагал, что в 
  горах находится около 6 тыс. партизан), — это также говорит об интенсивности 
  партизанских действий. Согласно донесениям штаба Мокроусова, за два месяца боёв 
  партизанами было уничтожено свыше 600 солдат и офицеров противника[14].<br>
  <br>
  Тем не менее, партизанам пришлось не легче, чем тем, против кого они боролись. 
  Дело здесь было не только в том, что командование 11-й армии быстро оценило 
  размах угрозы и организовало активное противодействие партизанам, но и с целым 
  рядом других обстоятельств. Несмотря на то, что подготовка партизанского движения 
  началась заблаговременно, уже с началом оккупации партизаны столкнулись с большими 
  трудностями. Ситуация, в которой они оказались, была совсем не похожа на ту, 
  которая имела место в период Гражданской войны. Местность, хотя сильно пересечённая 
  и покрытая лесом, в действительности не представляла надёжного укрытия, будучи 
  небольшой по площади и насквозь прорезанной дорогами. Отрядам, состоявшим преимущественно 
  из невоенных, пришлось вести боевые действия высокой интенсивности с хорошо 
  подготовленным и оснащенным противником, вовсе не походившим на врангелевскую 
  «стражу» периода Гражданской войны.<br>
  <br>
  Ещё одним исключительно важным фактором, осложнившим деятельность партизан, 
  стало внезапно проявившееся и не прогнозированное ранее отношение определённой 
  части местного населения к оккупантам, это касалось не только отдельных лиц, 
  но целых групп этого населения. В частности, Манштейн в своих воспоминаниях 
  отмечал следующее: «Татары сразу же встали на нашу сторону. Они видели в нас 
  своих освободителей от большевистского ига, тем более что мы уважали их религиозные 
  обычаи. Ко мне прибыла татарская депутация, принесшая фрукты и красивые ткани 
  ручной работы для освободителя татар «Адольфа Эффенди»[15]. Такие дружественные 
  встречи имели место по всему Крыму, например, командир (затем боец) Судакского 
  партизанского отряда Э. Юсуфов в своём донесении сообщал: «При оккупации немецкой 
  армией Крыма, в частности Судакского района, по данным разведки, в дер.: Ай-Серез, 
  Ворон, Шелен, Кутлак, в особенности в Отузах, со стороны большинства населения 
  была организована специальная встреча немцам. Встреча совершалась букетами винограда, 
  угощением фруктами, вином и т. д. В это число деревень можно отнести и дер. 
  Капсихор…»[16].<br>
  <br>
  О причинах, толкнувших значительную часть крымскотатарского населения к сотрудничеству 
  с немцами, написано достаточно много[17], однако наиболее красноречиво об этом 
  говорят немецкие документы: «До 1926—1927 гг. советы как-то считались с татарскими 
  традициями, — сообщалось в одном из донесений, — но после того как сюда начали 
  поселяться евреи, татары начали протестовать, а это вызвало подавление татарских 
  элементов, и особенно за то, что они упорно держались своей веры, языка, своей 
  культуры, своего уклада жизни. Их религиозная деятельность беззастенчиво подавлялась. 
  Это всё является, — делал вывод автор донесения, — главным основанием того, 
  что татары, в своей широкой массе крестьянское население, были чужды большевизму, 
  а власть имущие советские господа считали их людьми низшего сорта»[18]. Манштейн, 
  как и другие немецкие источники, изображает дело таким образом, что местные 
  деятели из числа крымских татар сделали первый шаг навстречу «освободителям», 
  однако, судя по всему, этому предшествовала определённая агентурная работа сторонников 
  германской ориентации в крымскотатарской среде, прежде всего в эмиграции[19]. 
  Так это было или нет, но командование 11-й армии незамедлительно воспользовалось 
  этой ситуацией, понимая, что она ценна не только созданием дружественной атмосферы 
  для немецких войск, но и возможностью использовать это дружеское отношение в 
  борьбе с противником. О том, какое это имело значение, говорит то обстоятельство, 
  что зона непосредственной дислокации партизанских отрядов находилась на территории 
  Алуштинского, Балаклавского, Бахчисарайского, Зуйского, Карасубазарского, Куйбышевского, 
  Старо-Крымского, Судакского, Ялтинского и части Симферопольского районов, где 
  как раз и была сосредоточена основная часть крымскотатарского населения[20]. 
  Со своей стороны германское командование оперативно предприняло меры для того, 
  чтобы расположить крымских татар к сотрудничеству. В одном из первых приказов 
  Манштейна после вступления частей его армии в Крым говорилось: «Всем задействованным 
  войскам против партизан еще раз довести до каждого, что в этом деле важна помощь 
  гражданского населения, особенно татар и мусульман, ненавидящих русских… Для 
  этого необходимо не допускать каких-либо неоправданных действий против мирного 
  населения. Особенно требуется корректное обращение по отношению к женщине. Необходимо 
  постоянно уважать семейные традиции татар и мусульман и их религию. Также требую 
  неукоснительного уважения к личному имуществу, сохранности скота, продовольственных 
  запасов сель¬ских жителей»[21]. Вскоре с санкции и помощью немецкого военного 
  руководства активисты коллаборационистского движения развернули деятельность 
  по формированию органов национального самоуправления, вовлекая в неё всё более 
  широкие слои населения.<br>
  <br>
  Эта ситуация оказалась полной неожиданностью для руководства эвакуированного 
  сначала в Краснодар, а затем в Сочи Крымского обкома ВКП(б) и для непосредственного 
  командования партизанскими силами в Крыму. Вслед за этим начались и другие трудности.<br>
