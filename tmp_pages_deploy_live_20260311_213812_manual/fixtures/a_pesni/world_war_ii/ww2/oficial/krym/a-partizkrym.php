  Как пишет О. Романько, еще в начале 1942 г. на территории рейхскомиссариата 
  немецкие военные власти начали организовывать из местного населения вспомогательные 
  полицейские батальоны «Schuma». В июле 1942 г. после полного установления немецкого 
  господства в Крыму было решено создать такие батальоны и здесь на основе существовавших 
  рот. Батальоны задумывались как оперативные подразделения для борьбы с партизанами, 
  в отличие от рот самообороны, более мобильные и мощные. К ноябрю 1942 г. было 
  создано 8 батальонов, которые расквартировали в следующих населённых пунктах: 
  №147 и №154 — в Симферополе, №148 — в Карасубазаре, №149 — в Бахчисарае, №150 
  — в Ялте, №151 — в Алуште, №152 — в Джанкое, №153 — в Феодосии. Имеются сведения 
  о том, что несколько позже было сформировано еще два батальона. Эти боевые единицы 
  имели штатную численность 501 человек, их военнослужащие носили немецкую форму, 
  были вооружены немецким оружием, имели лёгкие и тяжелые пулемёты и миномёты. 
  За каждым батальоном был закреплен свой «сектор ответственности» в горно-лесной 
  зоне Крыма. Эти подразделения также активно действовали против партизан во второй 
  половине 1942—1943 гг. С лета 1943 г. в связи с изменением ситуации на советско-германском 
  фронте после Сталинграда и Курска, а также в результате деятельности Крымского 
  штаба партизанского движения по разложению коллаборационистских воинских формирований 
  часть военнослужащих этих батальонов стала переходить к партизанам.<br>
  <br>
  <strong>6</strong><br>
  <br>
  Говоря об участии представителей крымскотатарского населения в коллаборационистских 
  акциях, было бы несправедливым не осветить вопрос о тех жертвах, которые понесли 
  крымские татары, остававшиеся в партизанских отрядах и помогавшие партизанам. 
  К великому сожалению, имена этих героев до недавнего времени были преданы забвению. 
  А между тем среди крымских татар были и те, кто не поддался немецкой пропаганде 
  и до конца выполнил свой долг перед Родиной. Их подвиг тем более значителен, 
  что они, оказавшись в меньшинстве, испытывали враждебность со стороны соплеменников 
  и подозрительность со стороны товарищей. Сегодня невозможно точно сказать, какое 
  количество крымских татар оставалось в партизанских отрядах весной и летом 1942 
  г., речь может идти, вероятно, о нескольких десятках человек. Более точно нам 
  известны потери. В боях 1941—42 гг. погибли, умерли от ран и голода, а также 
  пропали без вести, согласно данным Книги Памяти Республики Крым, — 113 крымских 
  татар-партизан (всего за весь период боевых действий из 3484 погибших и пропавших 
  без вести партизан значится 211 крымских татар[76]), в том числе бывший командир 
  Судакского отряда Э. Юсуфов, командир 5-го Красноармейского отряда А. Аединов, 
  командир Сейтлерского отряда Мухамедьяров. Вероятнее всего, это не исчерпывающие 
  данные, например, среди имён погибших нет командира Балаклавского отряда Г. 
  Газиева, убитого в бою в начале1942 г. Какое-то количество бойцов — крымских 
  татар было послано в 1942 г. «на оседание» в населённых пунктах для ведения 
  подпольной работы — среди них был и погибший в 1943 г. руководитель подпольной 
  организации в Симферополе Абдулла Дагджи. Часть партизан из крымских татар была 
  вывезена на Большую землю (в частности, Б. Османов). Всего из 262 партизан, 
  представлявших партизанские силы Крыма к лету 1943 г., в составе отрядов 1-го 
  и 2-го секторов было 6 крымских татар: Н. Белялов, М. Молочников, С. Кадыев, 
  К. Муратов, А. Аширов и прилетевший осенью 1942 г. Р. Мустафаев. (Состав партизан 
  1-го и 2-го сектора к 1 июня 1943 г.: 262 партизана, по национальности: русских 
  — 146, украинцев — 67, белорусов — 6, казахов — 2, латышей — 1, узбеков — 3, 
  таджиков — 1, татар — 6, испанцев — 2[77]).<br>
  <br>
  Несмотря на широкое распространение пронемецких настроений в крымскотатарской 
  среде, какое-то количество крымских татар их не разделяло и продолжало сотрудничать 
  с партизанами. Согласно данным, сообщенным комиссаром Судакского отряда А. Османовым: 
  «Хорошо помогали нам в связи с деревнями и городом 68-летний старик Смаил Баяк 
  из дер. Суук-Су, которого немцы и румыны несколько раз избили за подозрение 
  в связи с партизанами. Асан Белял из дер. Ай-Серез, Джанай Усеин, бывший счетовод 
  колхоза им. Молотова дер. Кутлак и др. Через этих товарищей мы получали данные 
  о наличии в деревнях немецких войск, движении немецкой армии и настроении солдат 
  и офицеров. Также узнавали настроение населения. Через этих же товарищей… мы 
  получали продукты и фрукты»[78]. Некоторые из этих людей поплатились за связь 
  с партизанами жизнью. Оккупационными спецслужбами были расстреляны А. Белял 
  (Ай-Серес), А. Асанов (Отузы), А. Меджитов (Козы)[79]. Хотя в условиях немецкой 
  пропаганды и террора активно помогать партизанам отваживались лишь отдельные 
  люди и немногочисленные группы, всё же в некоторых случаях партизаны встречали 
  лояльное отношение к себе со стороны населения целых деревень, в том числе и 
  с крымскотатарским населением. Больше всего их было в зоне действий партизанских 
  отрядов второго района. Лояльное отношение к партизанам отмечалось, по крайней 
  мере, весной 1942 г. в сёлах: Айлянма, Чермалык, Пролом, Алач, Карачёль, Аблеш, 
  Кокташ, где значительную, а иногда и большую часть населения составляли крымские 
  татары, и возможно в некоторых других населённых пунктах. Командир четвёртого 
  объединённого района И. Вергасов сообщал о том, что к весне 1942 г. в зоне деятельности 
  его отрядов: «…остались полностью наши сёла: Чаир, Лаки, Керменчик, которые 
  помогали партизанам[80]», из них в последнем проживало много крымских татар. 
  На стыке 4-го и 5-го районов оказывали содействие партизанам жители крымскотатарской 
  деревни Маркур, которые создали подпольную группу. «Больше полгода, — сообщал 
  Б. Османов, — дер. Маркур продолжала давать нам ценные сведения о противнике 
  и бесплатно снабжала весь отряд табаком. Вооруженных добровольцев в деревне 
  не было»[81]. <br>
  <br>
  <img src="a-partizkrym12.jpg" width="198" height="125"><br>
  <br>
  Согласно донесения Э. Юсуфова: «хорошее отношение к партизанам и десанту (Судакскому. 
  — Авт.) можно было заметить со стороны населения дер. Козы, которая является 
  и поныне (донесение датировано 2 ноября 1942 г. — Авт.) нейтральной»[82]. Впоследствии, 
  в 1943 г. количество таких деревень увеличилось, что дало основание германскому 
  военному командованию осенью и зимой 1943—44 гг. произвести отселение их жителей 
  за пределы горно-лесной зоны, а деревни сжечь. Любопытно, что в ряде случаев, 
  между партизанами и местным татарским населением имели место случаи довольно 
  интенсивных «товарно-денежных» отношений. В своём донесении «Некоторые итоги 
  за период боевой деятельности первого района» его тогдашний командир И. Чуб 
  сообщает, что во время скрытной днёвки в районе Судака отрядов этого района 
  при попытке эвакуации в сентябре 1942 г. имел место следующий случай: «вблизи 
  нашего расположения оказалось стадо барашек, но так как у нас общий закон — 
  не трогать барашек населения, мы решили послать ответственных товарищей из отрядов 
  к чабанам купить у них барашек за деньги. Во главе ответственных товарищей пошел 
  уполномоченный особого отдела Валиулин, который договорился с чабанами, что 
  они отпустят несколько барашек за деньги и за некоторые ценности. Им дали часы, 
  много немецких и румынских денег, за что взяли несколько штук барашек, разделили 
  по отрядам и здесь же отряды крепко подкрепились».<br>
  <br>
  Иногда сочувственное отношение к партизанам можно было встретить даже в среде 
  «добровольцев». Так, комиссар Бахчисарайского отряда Черный сообщал о том, что 
  двое из завербованных в «дружину самообороны» жителей села Биюк-Озенбаш — бывший 
  секретарь сельсовета Эдем и бригадир табаководов Ибраим — «…взяв оружие, всё 
  же оставались нам верными»[83]. Они предупреждали партизан о готовящихся нападениях 
  и делились с ними продуктами. В 1941—42 гг. такие случаи были единичными, однако 
  то обстоятельство, что со второй половины 1943 г. некоторая часть «добровольцев» 
  начала переходить к партизанам, говорит о том, что далеко не все участники этих 
  формирований были «идейными борцами с большевизмом».<br>
  <br>
  <strong>7</strong><br>
  <br>
  Оккупационные власти проводили довольно умелую политику стравливания местного 
  татарского населения и партизан. Использование населения для растаскивания баз 
  должно было спровоцировать партизан на проведение репрессивных действий, а последние 
  привести к созданию и вооружению отрядов самообороны. В этом же направлении 
  работала и немецкая пропаганда, главной задачей которой было вызвать страх местного 
  татарского населения перед партизанами. В целом до лета 1943 г. немцам удавалось 
  выполнять поставленную задачу. Население было убеждено, что партизаны являются 
  смертельными врагами крымских татар. Насколько сами партизаны «помогали» немцам 
  создавать такую картину? Другими словами, насколько действия партизан провоцировали 
  такое отношение к ним со стороны местного населения? Некоторые работы, выходящие 
  из-под пера крымскотатарских публицистов, рисуют картину едва ли не террора 
  со стороны партизан по отношению к татарскому населению, который, якобы, и толкал 
  крымских татар к сотрудничеству с оккупантами[84]. Знакомство с документами 
  говорит, что это было далеко не так. Конечно, партизанская война — вещь специфическая, 
  это жестокая война «без правил», в которой часто страдает местное население. 
  Но здесь следует различать: случаи спонтанной жестокости, вызванные обстоятельствами; 
  проявления неорганизованного массового насилия и, например, планомерное осуществление 
  репрессивных акций против «враждебно настроенного населения» по приказу командования. 
  Анализируя ситуацию в Крыму, можно сказать, что здесь наиболее многочисленны 
  факты первого, но не второго и тем более третьего типа. Вообще, говоря о партизанском 
  движении в период Великой Отечественной войны, следует помнить, что партизаны 
  этого периода вовсе не походили на вольные ватаги «повстанцев» времён Гражданской 
  войны. Дисциплина в партизанских отрядах поддерживалась не хуже, чем в регулярной 
  армии, а иногда и более суровыми мерами. Партизанское движение создавалось при 
  непосредственном участии органов НКВД, как правило, в каждом отряде численностью 
  даже меньше роты находился «особист», одной из задач которого был контроль за 
  поведением каждого партизана, это если и не исключало проявлений «вольного разбойничества», 
  то ограничивало его. Весьма характерный факт — за военные преступления только 
  в отрядах 3-го района за 1942 г. было расстреляно 62 человека[85]. Н. Луговой 
  — комиссар Зуйского отряда, а затем всех партизанских сил Крыма — в своих недавно 
  вышедших мемуарах, основанных на документах и дневниковых записях периода войны, 
  рассказывает историю командиров групп Зуйского отряда Мизько и Мозгова, расстрелявших 
  в феврале 1942 г. семью старосты с. Фриденталь Гриня (включая двух детей). Когда 
  об этом стало известно Мокроусову, он отдал приказ о немедленном расстреле партизан 
  за самоуправство и нарушение законности. Этот приказ лишь в последний момент 
  не был приведён в исполнение (засчитан условным), поскольку обвиняемым удалось 
  доказать, что семья Гриня знала членов семей партизан отряда и угрожала их выдачей 
  немцам[86]. Это говорит о том, что даже по отношению к семьям активных коллаборационистов 
  неконтролируемое насилие не поощрялось.<br>
