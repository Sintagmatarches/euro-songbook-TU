  <br>
  Командование 11-й армии довольно высоко оценивало подобного рода услуги. Так, 
  в «Положении штаба по борьбе с партизанами об организации операций против партизан 
  в Крыму» (31.01.42 г.) отмечалось, что «татарские стрелковые роты» «показали 
  себя неустрашимыми и умелыми борцами против партизан[73]». Другой немецкий документ 
  свидетельствует: «Немецкие воспитатели говорят, что татары очень горды тем, 
  что им доверяется такая служба… Они гордятся тем, что носят немецкую форму…[74]». 
  Успехи крымскотатарских подразделений отметил и лично фюрер. Выступая 24 мая 
  1942 г. в рейхстаге, Гитлер в своей речи заявил, что «в частях германской армии, 
  наряду с литовскими, латышскими, эстонскими и украинскими легионами, принимают 
  участие в боях с большевиками также татарские вспомогательные войска… Крымские 
  татары всегда отличались своей военной доблестью и готовностью сражаться. Однако 
  при большевистском господстве им нельзя было проявить этих качеств… Вполне понятно, 
  что они плечом к плечу стоят с солдатами германской армии в борьбе против большевизма[75]». 
  Вскоре крымскотатарские подразделения подверглись новой реорганизации, призванной 
  повысить их боеспособность.<br>
