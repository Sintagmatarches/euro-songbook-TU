  Анализ последнего донесения не позволяет упрекать Мокроусова в попытке возложить 
  какую-либо вину специально на крымских татар, он, как и в мартовском донесении, 
  говорит лишь о населении горной зоны и подчеркивает, что в степи настроения 
  другие. Любопытно, что в своих донесениях Мокроусов не сообщает о существовании 
  местного националистического центра и вообще не говорит о том, что происходит 
  в крымскотатарской среде, хотя разведывательная информация о широкой деятельности 
  немцев по созданию мусульманских комитетов уже была. Донесения Мокроусова и 
  Мартынова были не единственным источником информации, которым пользовался обком 
  и командование Крымского, а затем Северокавказского фронтов, информация шла 
  и по линии военной разведки, и по линии НКВД. При этом зачастую эта информация, 
  во всяком случае по «татарскому вопросу», носила и более обстоятельный и более 
  жесткий характер, по сравнению с чем донесения Мокроусова можно считать образцом 
  сдержанности.<br>
  <br>
  Сдержанность в оценке «крымскотатарского фактора» можно объяснить тем, что в 
  переписке Мокроусов имел дело с обкомом ВКП(б) Крымской АССР, состоявшим в значительной 
  степени из крымских татар, и знал, что будет неправильно понят, если станет 
  выдвигать татарский вопрос как ключевой (это, однако, впоследствии ему не помогло), 
  но, судя по всему, «татарский фактор» при всей его сложности не казался чем-то 
  фатальным и ему самому. Как же тогда быть с попытками Мокроусова «оправдаться», 
  переложить ответственность за неудачи? Всё обстоит достаточно просто. Мокроусову 
  и не в чем было оправдываться, потому что до падения Севастополя о неудачах 
  партизанского движения в Крыму не говорилось. В тяжелейших условиях и несмотря 
  на жертвы, партизаны действовали достаточно эффективно. Именно об этом свидетельствуют 
  признания Манштейна, который всегда отдавал должное своим врагам. «Всё время, 
  что я был в Крыму (до августа 1942 года), мы не могли справиться с опасностью 
  со стороны партизан»[114].<br>
