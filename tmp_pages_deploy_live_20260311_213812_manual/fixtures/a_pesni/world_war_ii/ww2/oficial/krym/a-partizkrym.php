  <br>
  Вообще сегодня, спустя более полувека после тех событий, очень легко рассуждать 
  об ошибках или недостаточном джентльменстве партизан. Интересно, как бы повели 
  себя сегодняшние критики в условиях, аналогичных тем, в которых оказались партизаны 
  Крыма — окруженные регулярными во много раз превосходящими по численности и 
  мощи частями противника, лишенные продовольствия и в окружении, скажем так, 
  «несколько недружественного населения». Тем не менее ситуация вовсе не обстояла 
  таким образом, что, столкнувшись с враждебностью населения, партизаны отвечали 
  только огнем на огонь. Даже в тяжелейших условиях 1942 г. руководство отрядами 
  предпринимало попытки наладить агитационную работу среди местного населения 
  и убедить его, по крайней мере, не выступать активно против партизан. Об этом 
  свидетельствуют как партизанские, так и немецкие документы[104].<br>
  <br>
  <img src="a-partizkrym16.jpg" width="198" height="187"><br>
  <br>
  Партизанское руководство выпускало листовки, публиковало в газете «Партизан 
  Крыма» обращения к крымским татарам, которые затем распространялись в деревнях. 
  На действовавшие лесные дороги отправлялись партизанские агитаторы — крымские 
  татары, которые вели пропаганду среди проходивших крестьян и т. д. Другое дело, 
  что этих усилий было крайне недостаточно, и это обстоятельство признавалось 
  самим партизанским командованием, у которого не было в тот момент реальных возможностей 
  для наращивания пропаганды.<br>
  <br>
  <strong>8</strong><br>
  <br>
  Как же в действительности непосредственное руководство партизанским движением 
  оценивало проблему враждебного отношения к себе со стороны определённой части 
  крымскотатарского населения? В послевоенной полемической крымскотатарской публицистике 
  утвердилось представление, которое достаточно активно тиражируется сегодня средствами 
  массовой информации, о том, что руководители крымского центра — Мокроусов и 
  Мартынов — «пытались переложить ответственность за неудачи на плечи крымских 
  татар». Обращение к документам ясно показывает, что ничего похожего на сознательное 
  нагнетание татарофобии и стремления «свалить всю вину на татар», как об этом 
  пишут некоторые публицисты, в донесениях, которые направляли Мокроусов и Мартынов 
  в центр, нет. Напротив, на начальном этапе партизанской борьбы, до марта 1942 
  г., мы можем говорить, скорее, о недооценке командующим и его комиссаром «татарского 
  фактора», чем о преувеличении его роли. В своем первом донесении о результатах 
  деятельности партизан в ноябре 1941 г. (составлено в декабре 1941 г.) Мокроусов 
  и Мартынов ничего не сообщают о поведении крымских татар: «Нами расстреляны 
  как предатели, — сообщают они, — служившие проводниками у немцев: Анохин, инженер 
  Бешуй-Копи, Налимский, житель деревни Саблы и секретарь Бешуйского сельсовета…»[105]. 
  Здесь же сообщается о создании в Симферополе большой «группы содействия гестапо 
  из числа разложившихся элементов, раскулаченных и других бывших людей»[106], 
  но не говорится ни слова о крымских татарах.<br>
  <br>
  Во втором отчете («Докладная записка за период с 1 ноября по 1 января 1942 г.) 
  о двух месяцах боевых действий (от 8 января 1942 г.) Мокроусов и Мартынов уже 
  отмечают появившуюся проблему, однако еще весьма осторожно. Сообщив о состоянии 
  партизанских отрядов и о боевых действиях и потерях, руководство п. д. сообщает, 
  что «гестапо создаёт в населенных пунктах вооруженные отряды из б (ывшего) кулацкого, 
  уголовного элемента, а также и дезертиров с целью борьбы против партизан. Такие 
  отряды носят название народная милиция. Такие отряды созданы в населённых пунктах 
  Коуш, Корбек, Улу-Узень и других пунктах»[107]. Далее сообщается, что наибольшую 
  активность проявил коушанский отряд. Мокроусову и Мартынову было, конечно, хорошо 
  известно, что этот отряд состоит практически целиком из крымских татар, однако, 
  судя по тексту донесения, они старательно избегают выставлять в нём «национальный 
  момент» и давать какие-либо оценки, это стремление просто бросается в глаза 
  при чтении донесения. Так, они ничего не говорят об отношении к партизанам в 
  татарских деревнях, но сообщают, что «в последнее время заметно выросли симпатии 
  к партизанам не татарских населенных пунктов (выделено нами. — Авт.)». Мокроусов 
  сообщает, что дезертирство из отрядов достигло 40%, но не уточняет национальный 
  состав дезертиров. Лишь в конце донесения он возвращается к татарской теме: 
  «По имеющимся данным в населенных татарских пунктах находится большое количество 
  дезертиров из частей Красной Армии, как, например, из деревни Коуш было призвано 
  130 человек татар, а не вернулось в село только 8 человек, в дер. Бешуй было 
  призвано 98 человек, а не вернулось 6 человек, аналогичное положение в других 
  населенных татарских пунктах»[108], но это сообщается вне всякой связи с информацией 
  о добровольческих подразделениях. Ситуация с крымскими татарами явилась полной 
  неожиданностью для командования партизанскими силами. Мокроусов предвидел даже 
  то, что столкнется с проблемой подчинения себе, отбившихся от своих частей отступающих 
  военных, но не то, что ему предстоит воевать с местными татарами.<br>
  <br>
  Лишь в мартовском донесении в центр («Краткий отчётный доклад о деятельности 
  партизанских отрядов Крыма с 1.11.41 по 21.03.1942 г.») крымское руководство 
  уже по-настоящему бьет тревогу: «В подавляющей своей массе татарское население 
  в предгорных, горных селениях настроено профашистски, из числа жителей которых 
  гестапо создало отряды добровольцев, используемые в настоящее время для борьбы 
  с партизанами, а в дальнейшем не исключена возможность и против Красной Армии»[109]. 
  Далее характеризуется отношение к партизанам различных этнических групп Крыма, 
  причем сообщается, что «татарское население степных районов, русские и греки 
  с нетерпением ждут прихода Красной Армии, помогают партизанам. Болгары занимают 
  выжидательную позицию»,[110] и делается вывод о том, что «деятельность партизанских 
  отрядов осложняется необходимостью вооруженной борьбы на два фронта: против 
  фашистских оккупантов, с одной стороны, и против вооруженных банд горно-лесных 
  татарских селений». Таким образом, первоначально командование как бы не замечало 
  проблему, затем, не зная как ее оценить, говорило о ней неопределенно и, наконец, 
  назвало положение таким, как оно представлялось уже большинству партизан. При 
  этом Мокроусов и Мартынов старались, насколько это было возможным, сохранять, 
  как бы мы теперь сказали, политкорректность: в докладной записке от 2 июля 1942 
  г., указав, что немцы комплектуют штат старост и полицейских из «бывших кулаков, 
  татар и другого антисоветского элемента»[111], оба партизанских руководителя 
  всё же посчитали своим долгом отметить, что: «враг опирается только на местные 
  фашистские банды. Всё остальное население Крыма, составляющее абсолютное большинство…, 
  с ненавистью относится к оккупантам…[112]», зачисляя тем самым в антифашистски 
  настроенное население и большинство татар.<br>
  <br>
  Более того, в обширной докладной записке от 23 июля 1942 г., т.е. написанной 
  уже после отзыва Мокроусова и Мартынова, когда, казалось бы, эти руководители, 
  следуя логике их современных критиков, должны были перекладывать всю вину на 
  татар, мы не находим ничего подобного оправданиям. В документе присутствуют 
  только три небольших фрагмента, касающиеся татарского вопроса: это цитированные 
  уже цифры дезертиров, объяснение трудностей добывания продовольствия партизанами 
  тем, что партизаны располагались «… в окружении татарских сел, а татарское население 
  враждебно относилось к партизанам и вооружено немцами» и одно указание на татарское 
  население горных районов, которое «с первых дней занятия Крыма фашистами пошло 
  в подавляющей массе своей за фашистами, что исключало возможность вести нам 
  работу, кроме агентурной»[113]. <br>
