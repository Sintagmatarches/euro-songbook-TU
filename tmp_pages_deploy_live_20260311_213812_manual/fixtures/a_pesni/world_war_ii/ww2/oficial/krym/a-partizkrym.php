  <br>
  Итак, имеющиеся в нашем распоряжении документы позволяют сделать определённые 
  выводы о значении «татарского вопроса» для партизанского движения Крыма 1941—42 
  гг. и об основных путях его разрешения. Прежде всего, не вызывает сомнения, 
  что он явился серьёзным вызовом для партизан.<br>
  <br>
  Мы не станем утверждать, что в 1941—42 гг. на стороне оккупантов оказалось «большинство» 
  крымских татар и что сотрудничество имело «массовый» характер, прежде всего 
  потому, что такие категории, как «большинство» и «массовость» носят абстрактный 
  характер и не отражают существа политических процессов, в том числе и таких, 
  как коллаборационизм[141]. Сегодня нельзя провести социологическое исследование 
  для определения реальной демографической базы коллаборационистских сил. Скорее 
  всего, основная масса татарского крестьянства, как и вообще населения Крыма, 
  просто пассивно ожидали дальнейшего развития событий, на которые не могли повлиять. 
  Активно действовало, безусловно, меньшинство, которое, однако, стремилось к 
  тому, чтобы создать у германского военного командования впечатление, что они 
  выражают настроения широких масс. В набор методов, которыми это достигалось, 
  входили: террор, подкуп, обман и спекулирование на реальных или мнимых исторических 
  несправедливостях. В результате активистам коллаборационистского движения удалось 
  привлечь достаточно существенную общественную поддержку (или создать видимость 
  таковой) и собрать под гитлеровские знамёна около 10% активного, преимущественно 
  мужского населения (что было близко к количеству призывного контингента). Поскольку 
  количество завербованных в карательные подразделения крым¬ских татар приблизительно 
  в 5-6 раз превышало общее количество партизан и было в десятки раз больше, чем 
  количество крымских татар, остававшихся в партизанских отрядах (по крайней мере, 
  к весне 1942 г.), нет ничего удивительного в том, что у руководства и рядовых 
  участников движения сопротивления складывалось впечатление того, что против 
  них настроена «основная масса» крымскотатарского населения (тем более, что в 
  1941—42 гг. подобного рода подразделений из представителей других национальностей 
  в Крыму не было). Эта ситуация усугублялась тем, что крымскотатарское население, 
  которое, по утверждению некоторых источников, продолжало оставаться «советским», 
  было лишено возможности проявлять эти настроения, в то время как проявления 
  враждебности против партизан и военнослужащих Красной Армии получали широкую 
  огласку и поощрялись немецким командованием.<br>
  <br>
  Повторим, что каким бы не был реальный размах сотрудничества с оккупантами, 
  оно оказалось серьёзным испытанием для партизанского движения Крыма. Это обуславливалось 
  тем, что зона партизанской деятельности практически полностью находилась в окружении 
  крымскотатарского населения, и изменение отношения его существенной части к 
  Советской власти и тех, кто в данном случае ее представлял, чрезвычайно осложнило 
  деятельность партизанских отрядов. Это касалось как их маневренности, скрытности 
  их базирования, так и обеспечения продовольствием, разведданными и т. д. Разгром 
  немецко-румынскими войсками партизанских баз, к которому активно привлекалось 
  местное население, породил тяжелейший голод, унесший жизни сотен людей. Еще 
  большее значение имело создание для борьбы с партизанами добровольческих отрядов, 
  часть из которых носила иррегулярный характер, а часть являлась регулярными 
  подразделениями вермахта и СД.<br>
  <br>
  Такая ситуация не предвиделась и не прогнозировалась партизанским командованием, 
  она явилась неожиданностью для партизан и часто вызывала ответную реакцию в 
  виде нападений на крымскотатарские сёла, убийстве подозревавшихся в коллаборационизме 
  (иногда безосновательно) представителей местной власти. Однако следует признать, 
  что партизанские действия носили реактивный характер, они были вызваны голодом 
  и блокадой, иногда стремлением осуществить возмездие по отношению к перешедшим 
  на сторону врага, но они не планировались заранее и не имели широкого размаха. 
  Во всяком случае, партизаны не уничтожали мирное население и его жилища, в то 
  время как со стороны добровольческих карательных подразделений это имело место 
  по отношению к тем, кто поддерживал партизан.<br>
  <br>
  Характер имеющихся документов не подтверждает вывода о том, что непосредственное 
  партизанское руководство неадекватно оценило уровень опасности, порожденной 
  переходом части крымскотатарского населения на сторону оккупантов, существенно 
  преувеличивая её, скорее наоборот, можно говорить о недостаточно серьёзном отношении 
  в партизанском руководстве к «татарскому фактору». Во всяком случае очевидно, 
  что штаб партизанского движения не смог до осени 1942 г. выработать какую-либо 
  долговременную специальную политику по отношению к крымским татарам и в этом 
  отношении, по признанию самого А.В. Мокроусова, деятельность штаба партизан 
  была явно недостаточной.<br>
  <br>
  Такая политика была выработана Крымским обкомом ВКП(б) в Решении бюро обкома 
  от 18 ноября 1942 г. По своей сути это была политика разрушения массовой базы 
  коллаборационизма путём отрыва большей части крымскотатарского населения от 
  сотрудничавшей с оккупантами верхушки. С этой целью бюро обкома определяло, 
  что с самого начала коллаборационизм не имел сколько-нибудь значительной поддержки 
  у населения и объявлял выводы о противоположном неправильными и «политически 
  вредными». По существу это являлось слишком оптимистической оценкой ситуации, 
  противоречившей объективным данным, но только это позволяло в дальнейшем строить 
  отношения с крымскими татарами как бы с чистого листа. Эту же цель преследовала 
  и критика действий непосредственного партизанского руководства в лице Мокроусова 
  и Мартынова в «татарском вопросе». При этом позиция командования партизанскими 
  отрядами была воспринята и изложена не в полном соответствии с действительностью, 
  однако такая критика давала возможность Крымскому обкому продемонстрировать, 
  что верховное руководство партизанским движением не склонно преувеличивать ответственность 
  основной массы коллаборационистски настроенных крымских татар. Как показали 
  дальнейшие события, это была совершенно верная тактическая линия, которая в 
  сочетании с другими факторами (прежде всего ухудшением положения немецких войск 
  на советско-германском фронте) позволила расколоть коллаборационистское движение 
  и создать условия для перехода значительного количества участников вооруженных 
  подразделений, находившихся в составе и на службе немецкого командования, на 
  сторону партизан накануне и в ходе решающих сражений за Крымский полуостров 
  во второй половине 1943—44 гг. «С конца 1943 г., — отмечает российский исследователь 
  А.Р. Вяткин, — несколько сотен крымских татар влились в ряды партизан и вместе 
  с ними стали участвовать в боях с немецко-румынскими оккупантами. Именно к этому 
  времени относятся те бои и победы, которые охотно описываются в современных 
  крымскотатарских публикациях. Тогда же были сожжены за измену Гитлеру несколько 
  десятков татарских деревень, население которых частью разбежалось, а частью 
  было мобилизовано на принудительные работы»[142].<br>
  <br>
  Сложные взаимоотношения партизан и части представителей крымскотатарского населения, 
  однако, сыграли свою негативную роль в решении ГКО о принудительной высылке 
  всех крымских татар с территории полуострова в мае 1944 г. Несмотря на то, что 
  против партизан активно выступала приблизительно лишь одна десятая часть крымских 
  татар, депортации подверглись и все те, кто не мог и не должен был нести ответственности 
  за подобного рода действия. Это явилось следствием обострённого восприятия советским 
  руководством трагических событий 1941—42 гг. в Крыму и практикой коллективной 
  ответственности, процветавшей в СССР в сталинский период. Впоследствии, уже 
  на завершающей стадии существования СССР, эта практика получила соответствующую 
  оценку и была осуждена.<br>
