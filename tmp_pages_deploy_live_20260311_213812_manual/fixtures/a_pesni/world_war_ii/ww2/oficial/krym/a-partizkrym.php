  <em>«Крымский щит» (Krimschield). Нарукавный знак отличия, которым награждались 
  немецкие военнослужащие, участвовавшие в боях южнее Перекопского перешейка с 
  21 сентября 1941 по 4 июля 1942 г.</em><br>
  <br>
  С другой стороны, в условиях разворачивания военных действий на Кавказе, где 
  для коллаборационизма среда была еще более питательной, Крым становился своеобразным 
  источником для переноса этого явления на соседние пространства, что, собственно, 
  не скрывали и немцы. Всё это, безусловно, вызывало огромную тревогу как в Центральном 
  штабе, так и в крымском руководстве. Но главная причина, возможно, заключалась 
  в событиях, которым еще только предстояло развернуться… На следующий день после 
  заседания бюро обкома, 19 ноября, начиналось масштабное советское наступление 
  под Сталинградом, и в этих условиях активизация партизанской деятельности на 
  захваченных территориях приобретала особую важность. Еще 5 сентября 1942 г. 
  в приказе № 00189 эта задача была сформулирована Сталиным: «… разгром германских 
  войск может быть осуществлён только одновременными боевыми действиями Красной 
  Армии на фронте и мощными непрерывными ударами партизанских отрядов по врагу 
  с тыла». В соответствии с этим «необходимо прежде всего добиться, чтобы партизанское 
  движение развернулось еще шире и глубже, нужно, чтобы партизанская борьба охватила 
  широчайшие массы советского народа на оккупированной территории. Партизанское 
  движение должно стать всенародным. Это значит, что существующие сейчас партизанские 
  отряды не должны замыкаться, а втягивать в партизанскую борьбу всё более широкие 
  слои населения»[127]. <br>
  <br>
  С началом нового советского контрнаступления открывалась и новая страница партизанской 
  борьбы, а она, соответственно, требовала новых решений старых проблем. В Крыму 
  это имело совершенно особое значение. «Для дальнейшего развертывания партизанской 
  войны, — писал в одном из первых послевоенных очерков, посвященных партизанскому 
  движению на полуострове, бывший комиссар 2-го района Н. Луговой, — связь с населением 
  приобретала исключительную важность. Политическая работа в массах и мобилизация 
  их на борьбу с врагом, разведка вражеского тыла и диверсионная война на коммуникациях 
  противника, организация притока свежих сил в партизанские отряды и хотя бы частичное 
  снабжение партизан продовольствием, обмундированием и медикаментами — все это 
  было возможно только при опоре на местное население»[128], для налаживания диалога 
  с которым требовались особые, специальные усилия.<br>
  <br>
  В этих условиях для крымского обкома признание массового характера крымскотатарского 
  коллаборационизма в 1941—42 гг. было равносильно проявлению крайней политической 
  близорукости. Признав, что большинство крымских татар сотрудничают или благожелательствуют 
  немцам, обком тем самым не только расписался бы в собственной несостоятельности 
  за все 20 лет Советской власти, но и исключил бы саму возможность со временем 
  изменить ситуацию в свою пользу. Обкому никак нельзя было приравнивать татар 
  к врагам, тем самым окончательно толкая их в объятия оккупантам, следовало думать 
  не о том, кто прав, а кто виноват в сложившейся ситуации, не о констатации (и 
  следовательно, консервации) существующего положения вещей, а о том, как переломить 
  неблагоприятные тенденции. Это было тем более важно, что осенью 1942 г. из Крыма 
  стала мало-помалу просачиваться информация о том, что крымскотатарское население 
  вовсе не получает от немцев того, на что рассчитывало в начале оккупации, и 
  все чаще высказывает свое недовольство[129]. Изменяющиеся настроения следовало 
  использовать. Именно поэтому верховное партизанское руководство решило «закрыть 
  глаза» на коллаборационизм — только это позволяло начать с «татарским фактором» 
  игру «с чистого листа» и, как показали дальнейшие события, это был совершенно 
  правильный стратегический ход.<br>
  <br>
  Единственную трудность представлял объём негативной информации о крымскотатарском 
  коллаборационизме, накопившийся к этому времени, но выход был найден — обком 
  решил «взять вину на себя», точнее, возложить её на своих представителей в Крыму 
  А.В. Мокроусова и С.В. Мартынова, которые, якобы не разобравшись в ситуации, 
  «неправильно информировали обком» и ориентировали командиров отрядов (тем более, 
  что Мокроусова и Мартынова уже определили виновными из-за конфликта с военными). 
  Заседание бюро обкома и постановление представляли собой начало долговременной 
  политической и, если угодно, оперативно-контрразведывательной игры с крымскими 
  татарами, игры, которая в итоге должна была закончиться если не в перетягивании 
  их на свою сторону, то хотя бы в нейтрализации. Забегая вперед, следует признать, 
  что эта «спецоперация» в конце концов увенчалась успехом, правда, при этом пострадала 
  репутация Мокроусова. Но что для настоящих большевиков значило доброе имя одного 
  человека, если на карту ставился успех «дела»?<br>
  <br>
  Даже непредвзятый анализ текста постановления показывает, что это документ, 
  отражавший не столько реальное положение дел в Крыму, сколько определённые намерения 
  обкомовского руководства по его изменению.<br>
  <br>
