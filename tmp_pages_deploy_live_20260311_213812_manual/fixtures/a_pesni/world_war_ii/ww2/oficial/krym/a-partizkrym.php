  <strong>Андрей Мальгин, генеральный директор Крымского республиканского краеведческого 
  музея</strong><br>
  <br>
	<span class="header"><strong>РУКОВОДСТВО ПАРТИЗАНСКИМ ДВИЖЕНИЕМ КРЫМА В 1941—1942 
  ГОДАХ И «ТАТАРСКИЙ ВОПРОС» <br>
  </strong><br></span> 
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=97&par=74&l=&art=493" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, №14, 2006</em></a><em>.</em><br>
  <br>
  <br>
  Тема коллаборационизма вообще и в особенности в среде крымских татар в период 
  оккупации Крыма немецко-румынскими войсками в 1941—44 гг. до сих пор является 
  чрезвычайно болезненной для общественного мнения Крыма. Как известно, именно 
  обвинение в массовом сотрудничестве с немецко-фашистскими захватчиками послужило 
  обоснованием для принудительного выселения всех крымских татар с территории 
  полуострова 18 мая 1944 года. Затем, в 1967 г., это обвинение было снято как 
  «огульное», но, тем не менее, пересмотра проблемы в советской исторической литературе 
  не произошло, точно так же, как и разрешения для крымских татар на возвращение 
  в Крым. В советской и не только советской литературе о Второй мировой войне 
  прочно укоренилось представление о том, что «массовое сотрудничество» крымских 
  татар с оккупантами имело место, что, в частности, отразилось на трагической 
  истории партизанского движения в Крыму, которому пришлось вести борьбу с фашистами 
  в условиях фактически противодействия местного татарского населения. Напротив, 
  среди самих крымских татар, с 1953 года начавших борьбу за возвращение на свою 
  родину, укоренилось представление о том, что крымские татары стали жертвой клеветы, 
  что все обвинения в их адрес были сфальсифицированы и не имеют под собой оснований. 
  В этой связи «под ударом» теперь уже со стороны крымских татар оказалось руководство 
  партизанским движением Крыма, которому было выдвинуто обвинение в том, что оно 
  намеренно искажало действительную картину событий и пыталось переложить ответственность 
  за неудачи партизанского руководства на крымских татар. Эта точка зрения нашла 
  своё отражение в крымскотатарской полемической литературе 50-х — 90-х гг. Недавно 
  в несколько модернизированном виде она появилась в работах С.М. Червонной, В.Е. 
  Возгрина, Г. Бекировой[1] и др. Отстаивающие эту точку зрения публицисты (именно 
  публицисты, поскольку как раз в публицистическом ключе она главным образом и 
  освещалась), использовали для подтверждения своей правоты главным образом материалы 
  Решения бюро Крымского обкома ВКП(б) «Об ошибках, допущенных в оценке поведения 
  крымских татар по отношению к партизанам, о мерах по ликвидации этих ошибок 
  и усилению политической работы среди татарского населения», принятого еще 18 
  ноября 1942 г., но, по понятным причинам, оставшегося неизвестным широкой общественности.<br>
  <br>
  Существо этой точки зрения сводится к двум тезисам — первому: участие крымских 
  татар в партизанском движении преуменьшено, а в коллаборационистских акциях 
  — преувеличено, и второму: сравнительно редкие случаи коллаборационизма провоцировались 
  самими партизанами, а также имели корни в предшествующей истории.<br>
  <br>
  Как и противоположная точка зрения, этот подход до сих пор существует лишь в 
  виде тезисов, авторы которых не спешили обременять себя какими-либо серьезными 
  доказательствами правоты своих позиций.<br>
  <br>
  Пока ещё мы вынуждены констатировать острый недостаток обоснованных данных о 
  роли крымских татар (а также представителей других народов) в движении сопротивления 
  в Крыму, равно как и в коллаборационистских организациях и акциях в их среде. 
  Раньше это объяснялось закрытостью архивов, сегодня — тем, что какие-либо попытки 
  завязать серьёзный разговор на эту тему более чем настороженно воспринимается 
  как в научном сообществе Крыма, так и в крымском обществе в целом, поскольку 
  якобы способны вызвать «межнациональную напряженность». Это «табу», однако, 
  само по себе чрезвычайно вредит установлению взаимопонимания в современном Крыму, 
  поскольку изымает из его новейшей истории один из ключевых моментов. На наш 
  взгляд, настало время начать постепенно разбирать эти исторические «завалы». 
  Подобная работа требует, конечно, очень большой осторожности. С самого начала 
  всем должно быть ясно и понятно, что речь не идет и не может идти о каком-либо 
  варианте «оправдания» депортации крымских татар. История уже дала оценку этому 
  прискорбному и заслуживающему осуждения факту: какими бы ни были масштабы сотрудничества 
  с захватчиками в оккупированном Крыму, это не оправдывает репрессий против всего 
  крымскотатарского населения, которое в большинстве своем не совершало враждебных 
  действий против своего государства во время войны. Вместе с тем без оценки реальных 
  масштабов, характера и причин сотрудничества определенной части этого населения 
  с оккупантами всегда будет оставаться потенциальная возможность искажения правды 
  в угоду той или иной политической конъюнктуре[2]. Ясность в вопросе нужна как 
  крымским татарам, так и крымскому обществу в целом, поскольку отношения в нем 
  не могут строиться на основании недосказанностей и взаимных подозрений. Достойный 
  подражания образец в этом отношении дало новейшее совместное польско-украинское 
  историческое расследование событий на Волыни в 1942—43 гг., которое, несмотря 
  на всю болезненность этой темы для поляков с одной стороны и западных украинцев, 
  с другой, всё же стало важным шагом к взаимопониманию и примирению между ними. 
  Возможно, такой же проект нужен и Крыму, многие сегодняшние проблемы которого 
  корнями уходят в годы последней войны[3].<br>
  <br>
  Проблема, о которой мы говорим, имеет многоаспектный характер. Нам бы хотелось 
  затронуть лишь один, но чрезвычайно важный ее момент — появление так называемого 
  «татарского вопроса» перед руководством партизанского движения Крыма и решение 
  его в тяжелой обстановке кризиса партизанской борьбы осенью 1942 года. Именно 
  тогда между представителями оперативного руководства партизанами Крыма (прежде 
  всего командующим партизанскими силами А.В. Мокроусовым) и Крымским обкомом 
  ВКП(б), находившимся на Большой земле, впервые и возникла довольно острая полемика 
  о роли «татарского фактора» в идущей на территории Крымского полуострова борьбе 
  и о существе политики в отношении крымскотатарского населения, полемика, которую 
  нельзя признать завершенной и по настоящее время. Мы также далеки от мысли поставить 
  в ней окончательную точку, однако для того, чтобы это можно было сделать в будущем, 
  необходимо, как нам кажется, окунуться в сложную атмосферу, царившую в партизанском 
  Крыму в первый год оккупации полуострова немецко-фашистскими войсками[4].<br>
  <br>
  <strong>1</strong><br>
  <br>
  В отличие от большей части территории Советского Союза, стремительно захваченной 
  врагом, где партизанское движение фактически организовывалось уже в обстановке 
  оккупации, в Крыму эта работа началась заблаговременно. Еще в августе 1941 г. 
  Крымский ОК ВКП (б) (Секретарь В.С. Булатов) совместно с 4-м отделом НКВД Крымской 
  АССР в атмосфере строгой секретности начали работу по осуществлению директивы 
  Совнаркома и ЦК ВКП(б) от 29 июня 1941 г. о перестройке всей работы на военный 
  лад, включая организацию сопротивления на захваченных территориях, и Постановления 
  ЦК ВКП(б) от 18 июля 1941 г. «Об организации борьбы в тылу германских войск». 
  На случай возможного захвата Крыма вражескими войсками предполагалось развернуть 
  на территории полуострова мощное партизанское и подпольное движение под руководством 
  специально созданного подпольного обкома. 5—7,5 тыс. бойцов, снабдив их оружием, 
  боеприпасами и продовольствием сроком до полугода. Весь крымский лес был разбит 
  на пять оперативных районов, в которых должны были сосредоточиться 29 партизанских 
  отрядов (кроме этого три отряда из Ленинского, Маяк-Салынского районов и г. 
  Керчи должны были разместиться в каменоломнях Керченского полуострова). Отряды 
  комплектовались в каждом районе Автономной Республики силами местных партийных 
  комитетов из партийных, комсомольских активистов, специалистов, не призванных 
  к этому моменту в действующую армию, а также из находящихся в запасе военных. 
  Основой для создания отрядов стали так называемые «истребительные батальоны», 
  т.е. вспомогательные части, сформированные местными отделениями НКВД для борьбы 
  с диверсантами противника, охраны предприятий и военных объектов. Непосредственное 
  руководство возлагалось на командование, которое возглавил в конце октября командующий 
  «народным ополчением» Крыма подполковник А.В. Мокроусов, комиссаром был назначен 
  секретарь Симферопольского горкома С.В. Мартынов, начштаба — майор И.К. Сметанин. 
  Предполагалось, что общее руководство партизанским движением и подпольем будет 
  осуществлять нелегальный обком во главе с И.А. Козловым. К организации партизанских 
  отрядов были привлечены партийные деятели, имевшие опыт партизанской борьбы 
  во время Гражданской войны: И.Г. Генов, А.А. Сацук, П.В. Макаров, В.А. Красников 
  и др. Такой же опыт имел и Мокроусов — легендарный партизанский командир, действовавший 
  в тылу у белых и заслуживший похвальные отзывы о своей деятельности от самого 
  батьки Махно. Опыт прошлой войны был положен и в основу представлений о тактике 
  и стратегии партизанской борьбы. Предполагалось, что, как и в борьбе против 
  Врангеля, партизан будет поддерживать местное население (прежде всего крымскотатарское), 
  оккупанты ограничатся установлением контроля за городами и основными коммуникациями, 
  а в село, и тем более в горы, «не сунутся».<br>
  <br>
  <img src="a-partizkrym1.jpg" width="198" height="282"><br>
  <em>А.В. Мокроусов</em><br>
  <br>
  Поскольку отряды создавались из гражданского населения районов и городов, вероятнее 
  всего, что их первоначальный состав отражал национальную структуру населения. 
  По данным переписи 1939 г., в Крыму проживало 1.126.429 человек, из которых 
  русских было 49,59%, украинцев 13,68%, татар (имеются в виду крымские татары) 
  19,43%, евреев 5,81%, армян 1,15%, болгар 1,36%, греков 1,83%, прочих 2,61% 
  (немцы — 4,55% крымского населения, были выселены в августе 1941 г.). К сожалению, 
  у нас нет сегодня возможности с достаточной точностью определить национальный 
  состав партизанских отрядов Крыма, но крымские татары были представлены практически 
  во всех отрядах[5]. Значительное количество крымских татар было в Алуштинском, 
  Бахчисарайском партизанских отрядах. Были отряды, которые можно назвать целиком 
  крымскотатарскими: Куйбышевский (командир Ибраимов, комиссар Макъянов), Судакский 
  (командир Э. Юсуфов, комиссар А. Измаилов[6]), большое количество татар было 
  и в Балаклавском отряде, во всяком случае его командование состояло из крымских 
  татар (командир Г. Газиев, комиссар М. Беткелиев[7]). Из руководства районного 
  звена крымскими татарами были: комиссар первого района Аблязиз Османов и комиссар 
  четвертого района Мустафа Селимов (он был отозван Крымским обкомом накануне 
  начала партизанской борьбы и на первом её этапе в ней участия не принимал). 
  Уже в ходе боевых действий руководящие должности в партизанских отрядах занимали 
  А. Аединов (командир Красноармейского отряда 4-го района), А. Мухамедьяров (командир 
  Сейтлерского отряда). Трибунал, действовавший при партизанском штабе, с апреля 
  1942 г. возглавлял Нафе Билялов, его секретарём (фактически заместителем) был 
  Мемет Молочников.<br>
  <br>
  Документы и воспоминания не дают материалов, позволяющих говорить о каких-либо 
  сомнениях по привлечению крымских татар в партизанские отряды8, напротив, поскольку 
  руководство в них принадлежало ветеранам Гражданской войны, нужно полагать, 
  что оно благосклонно относилось к татарам — своим союзникам по борьбе с Врангелем. 
  В частности, уже на завершающем этапе подготовки движения командир 2-го района 
  И.Г. Генов предложил сформировать дополнительно три отряда из жителей крымскотатарских 
  деревень Капсихор, Ускут и Улу-Узень, и такие отряды были созданы [9], возник 
  отряд (или группа) из колхозников крымскотатарского села Стиля и, возможно, 
  другие. Более того, поскольку дислокация отрядов планировалась в горно-лесной 
  зоне в окружении крымскотатарских сёл, их население было с самого начала активно 
  привлечено к закладке тайных продовольственных баз.<br>
  <br>
  <strong>2</strong><br>
  <br>
  28 октября группировка войск противника под командованием генерал-лейтенанта 
  Эриха фон Манштейна в составе 11-й немецкой армии и частей 3-й румынской армии 
  прорвала ишуньские укрепления и вторглась на территорию полуострова. Разбитые 
  части советских 51-й и Приморской армий поспешно отступали, первая в направлении 
  на Керчь, вторая — на Севастополь. Республиканское руководство во главе с В.С. 
  Булатовым (Крымский обком ВКП(б) и Совнарком Крымской АССР) было эвакуировано 
  в Краснодар, а затем перебралось в Сочи. В эти же дни к местам расположения 
  в соответствии с заранее разработанным планом выдвинулась и большая часть партизанских 
  отрядов, штабы районов и главный штаб. К партизанам присоединилось также значительное 
  количество отставших от своих частей военнослужащих, которые частично вливались 
  в уже существовавшие отряды, а частью образовывали новые. В результате в ноябре 
  1941 г. в Крымских горах действовало 27 партизанских отрядов общей численностью 
  3.456 человек (из них около тысячи — военные). В тылу врага разворачивалась 
  масштабная партизанская война. О её размахе лучше всего сказал командующий немецко-румынскими 
  силами будущий фельдмаршал Манштейн:<br>
  <br>
  <em>«Партизаны стали реальной угрозой с того момента, когда мы захватили Крым 
  (в октябре-ноябре 1941 г.). Не может быть сомнения, что в Крыму существовала 
  весьма разветвлённая партизанская организация, которая создавалась долгое время. 
  Тридцать истребительных батальонов… представляли собой лишь часть этой организации. 
  Основная масса партизан находилась в горах Яйла. Там, вероятно, с самого начала 
  было много тысяч партизан… Партизаны пытались контролировать наши главные коммуникации. 
  Они нападали на мелкие подразделения или одиночные машины, и ночью одиночная 
  машина не смела показаться на дороге. Даже днём партизаны нападали на мелкие 
  подразделения и одиночные машины. В конце концов нам пришлось создать целую 
  систему своеобразных конвоев[10]».</em><br>
