  Уже первые дни партизанской деятельности показали, что далеко не всё соответствует 
  заранее разработанной стратегии. Прежде всего это касалось партизанских кадров. 
  Стремительный прорыв немцев перекопских укреплений привел к тому, что из 29 
  отрядов — четыре вообще не вышли к местам дислокации (Красноперекоп¬ский, Лариндорфский, 
  Фрайдорфский[22] и отряд работников НКВД, из которого явился только штабной 
  комендантский взвод). Не появился в лесу ни один созданный Геновым из жителей 
  крымскотатарских сёл отряд в зоне 2-го района[23]. Не пришли и некоторые из 
  партизанских руководителей, в частности комиссар 4-го р-на М. Селимов, отозванный 
  в последний момент в распоряжение обкома. Забазировавшиеся отряды сразу же столкнулись 
  с дезертирством, которое иногда принимало массовый характер (бывший боец, впоследствии 
  комиссар 6 -го партизанского отряда А.А. Сермуль рассказывал, что около трети 
  из списочного состава 3-го симферопольского отряда не выразила желания идти 
  в лес и вернулась домой уже с места сбора отряда 1 ноября 1941 г.[24]). Столкнувшись 
  с трудностями, часть партизан решила пробиваться к Севастополю, часть просто 
  расходилась по домам. Некоторые дезертиры переходили на службу оккупантам, становясь 
  проводниками, полицейскими и т. д. Иногда дезертирами оказывались довольно высокопоставленные 
  партизанские руководители, например начштаба 5-го района Иваненко[25], начальник 
  комендантского взвода центрального штаба Лукин[26], начштаба Бахчисарайского 
  отряда Достмамбетов[27], командир группы 2-го Симферопольского отряда Сайдашев[28], 
  комиссар Судакского отряда А. Измаилов, комиссар Балаклавского отряда Беткелиев, 
  командир Кировского отряда Алдаров[29] и некоторые другие. Известны случаи ухода 
  из мест расположения целыми отрядами. Так, в Севастополь после первого боестолкновения 
  с противником ушел Сакский партизанский отряд, Тельмановский отряд в дни занятия 
  Крыма немцами отправился из места дислокации (подорвав свои базы) в Ялту во 
  главе с командиром Гринбергом и его судьба осталась неизвестной. Достаточно 
  хорошо известна, благодаря докладной записке, которую оставил разведчик-связист 
  этого отряда Б. Османов (в дальнейшем видный деятель крымскотатарского национального 
  движения, отец Ю.Б. Османова, лидера НДКТ, погибшего в 1993 г.), история исчезновения 
  Куйбышевского отряда. Из нее явствует, что 1 ноября 1941 г. Куйбышевский партизанский 
  отряд, созданный на основе районного истребительного батальона, в составе 115 
  человек («не менее 100 человек татар, человек 6 русских, остальные из других 
  национальностей» — Б.О. ) под командованием инструктора РК ВКП (б) Ибраимова, 
  начштаба, председателя Куйбышевского райсовета Аметова, комиссара отряда, замдиректора 
  Фотисальской МТС Макъянова вышел к месту своей дислокации.<br>
  <br>
  «Отряд имел очень богатые базы, мука, макароны, а также медикаменты могли бы 
  обеспечить года на два весь контингент отряда… — сообщал Бекиров. — Командир 
  отряда Ибраимов, при первых же орудийных выстрелах из дер. Ангигуль, бросил 
  отряд и ушел в деревню, объявив партизанам, что кто желает, пусть идет домой: 
  «Я хорошо знаю какой немец — в 1918 году прошел отсюда со своими орудиями, грозной 
  техникой. А сейчас тем более нам не удержать его с нашим вооружением. Я иду 
  и никому не советую оставаться». С его уходом ушли свыше 50 человек, частично 
  разграбив базы отряда. После него командиром отряда стал Меджметдинов (по др. 
  данным Меджрединов) — работник милиции. (...) Командование отрядом… неофициальным 
  путем отдало приказание всем желающим возвратиться домой, что ускорило разгром 
  отряда немцами, так как все неблагонадежные лица, возвратившись, связались с 
  немцами и указали им пути подхода… Работа среди партизан не велась.13 ноября 
  1941 г. немцы внезапно напали на отряд, застав врасплох бойцов. Работники штаба 
  в этот момент оказались в стороне с группой красноармейцев (…), желая вместе 
  с ними пробиться в Севастополь. Отряд бой не принял, за исключением меня, Лазинского, 
  Супруна и Бекирова[30]. После этого Куйбышевский отряд больше не собирался. 
  … Я имел связь со всеми отрядами Крыма, и узнал, что ни в одном из них не имеется 
  ни одного партизана из Куйбышевского отряда»[31].<br>
  <br>
  Аналогичная история произошла и с партизанским отрядом (или группой), созданным 
  из жителей деревни Стиля[32].<br>
  <br>
  В своем донесении Мокроусов называет общую цифру дезертировавших в 1200 человек, 
  сообщая, что в первые два месяца дезертировал 891 человек, «в основном татары»[33]. 
  Информация о том, что дезертировали главным образом татары, содержится в нескольких 
  документах. Об этом, в частности, говорит И. Вергасов, командир 4-го района, 
  в беседе с секретарём обкома Булатовым после эвакуации на Большую землю. На 
  вопрос секретаря обкома, сколько в 4-м районе было татар, Вергасов отвечает: 
  «Всего татар было 130 чел. до слияния с 5-м районом (оно произошло в марте 1942 
  г.). Татары пятого района никакими боевыми действиями не занимались, никакие 
  операции не производили, за исключением некоторых. Уже при мне дезертировало 
  40 человек, при этом ушла основная масса татар. Осталось 2—3 человека. Из 40 
  человек дезертиров было 30 татар, главным образом в Балаклавском отряде»[34]. 
  <br>
  <br>
  <img src="a-partizkrym5.jpg" width="198" height="134"><br>
  <br>
  Надо думать, речь идет о дезертирстве из двух отрядов 5-го района, влитых в 
  4-й район. О дезертирстве партизан-татар из отрядов своего, 4-го района, Вергасов 
  не сообщает. Аналогичную картину рисует в своей докладной записке и замначальника 
  ОО центрального штаба Попов: «Когда мы первые дни находились в лесу, то в отдельных 
  партизанских отрядах было большое количество татар. Например, в Алуштинском 
  районе (здесь не вполне ясно, идёт ли речь об Алуштинском партизанском отряде 
  или о 3-м районе, в который входил этот отряд. — Авт.) их было до 100 человек, 
  но в результате татары дезертировали из отрядов»[35]. Действительно ли дезертирство 
  приняло особый размах именно среди крымских татар? Документы не дают возможности 
  ни подтвердить, ни опровергнуть приведённые сведения — у нас нет списочных данных 
  по национальному составу дезертиров. А.А. Сермуль говорил, что из Симферопольского 
  отряда дезертировали «как татары, так и русские». И все же с большой долей вероятности 
  можно предположить, что партизаны-татары оказывались более склонными к дезертирству, 
  во-первых, потому что относительно крымских татар оккупационными властями проводилась 
  специальная политика, велась агитация и, во-вторых, потому что в отличие от 
  весьма значительного количества нетатар дезертирам-татарам было куда идти, они 
  представляли собой местное население, в то время как нетатары были либо выходцами 
  из других районов Крыма, либо вообще военными, отбившимися от своих частей или 
  попавшими в окружение. В отличие от всех других у татар был реальный выбор — 
  уходить или оставаться.<br>
  <br>
  Еще одну причину массового ухода крымских татар из отрядов приводит секретарь 
  Судакского райисполкома, комиссар 1-го района, затем Судакского отряда А. Османов: 
  «Второй причиной ухода партизан из отряда, — сообщает он в докладной записке, 
  — в особенности из татар, считаю, что примерно в весенние месяцы к татарским 
  товарищам партизанам не было никакого доверия. Следили за каждым партизаном, 
  часто упрекали их дезертирами, курултайщиками, предателями и т.д. Вследствие 
  такого положения только в мае месяце из ушедших из отряда 21 человека — 9 татар»[36]. 
  Весной 1942 г. национальная рознь проникла уже в сами партизанские отряды и 
  также провоцировала дезертирство.<br>
  <br>
  Еще в большей степени, чем дезертирство, ущерб партизанскому движению нанесла 
  потеря партизанскими отрядами продовольственных баз в конце 1941—42 гг. Собственно, 
  в конечном итоге именно она и вызванные ею трудности с продовольствием, переходящие 
  в голод, спровоцировали дезертирство из отрядов в столь широких масштабах. Согласно 
  данным Мокроусова, базы закладывались из расчета на питание 5—6 тыс. человек 
  сроком до полугода. Однако уже к началу 1942 года эти базы были в основном потеряны. 
  И. Вергасов объяснял это преступной халатностью ответственных за базирование 
  лиц: «Беда в том, что подбор людей, которые занимались базами, со стороны райкомов 
  и районных отделений НКВД был не партийный, а зачастую предательский. Чем иным 
  можно объяснить такие факты, как: базы располагались близко к сёлам, имели хорошие 
  подъезды для автотранспорта, а люди, которые заготовляли, в основной массе бежали 
  в первые дни оккупации»[37]. Разгром баз облегчался тем, что большое количество 
  продуктов не было увезено глубоко в лес, а сосредоточено на так называемых перевалочных 
  базах вблизи дорог. Ответственность за непосредственное разграбление баз партизаны 
  также возлагали на местное татарское население, поскольку, с одной стороны, 
  немцев к базам выводили часто местные крестьяне, которых привлекали к закладке, 
  а с другой — вывоз продуктов из леса с захваченных партизанских баз также осуществлялся 
  жителями деревень под охраной немецких или румынских войск. Согласно докладной 
  записке зам. начальника Особого Отдела центрального штаба Попова: «Основные 
  продовольственные базы партизанских отрядов были разграблены в декабре месяце 
  1941 г., главным образом благодаря предательству со стороны татар, которые являлись 
  не только проводниками немецких войск, но и сами принимали активное участие 
  в разгроме продовольственных баз. В Алуштинском отряде к организации продовольственных 
  баз были привлечены татары, которые в начале ноября месяца их предали, и отряд 
  всё время находился без продовольственной базы»[38]. Как именно происходило 
  разграбление баз отрядов, сообщает Вергасов: «Противник уже 10—13 ноября стал 
  с помощью предателей организовывать удар по самому чувствительному месту — на 
  продовольственные базы. Бахчисарайский отряд подвергся нападению, принял двухдневный 
  бой (убито противника 36 человек, своих (потеряли. — Авт.) 11 человек), но базу 
  противник забрал и под своей охраной разрешил грабить их местному населению, 
  выпустив листовку, что население осталось голодным из-за партизан. Ак-Мечетский 
  отряд без боя потерял свои базы к 25 ноября. Красноармейский вообще не имел 
  своих баз. Уже в конце ноября три отряда остались без питания»[39]. Подобное 
  же происходило, добавим, повсеместно. Таким образом, схема действий оккупантов 
  против партизанских баз была следующей: разведка, оттеснение от баз отрядов, 
  передача имущества баз населению. Насколько добровольным было участие населения 
  в вывозе продуктов из леса, мы не знаем, высказывалось мнение о том, что немцы 
  «насильно» заставляли население грабить партизанские базы[40], но это утверждение 
  не находит документального подтверждения — как правило, немцы действовали методом 
  агитации. Очевидно только, что организация и инициатива в разграблении партизанских 
  баз принадлежала немецким военным властям, а население «подтягивалось» в качестве 
  исполнителей. То есть непосредственный увоз продуктов осуществлялся именно населением. 
  Об этом, в частности, говорит в своём донесении от 2 ноября 1942 г. Э. Юсуфов: 
  «После, как базы Судакского отряда были разграблены гражданами окружающих деревень 
  (несколько ранее он перечисляет эти деревни: Ай-Серез, Ворон, Шелен, Кутлак, 
  Отузы, Суук-Су и г. Судак. — Авт.), а 28 июля были взяты барашки нашего отряда 
  и распределены: 100 голов дер. Ворон, часть Ай-Серез и воинским частям. Делилось, 
  главным образом, среди активных проводников и антисоветских элементов, служивших 
  у гестаповцев»[41]. <br>
