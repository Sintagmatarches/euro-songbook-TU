  <br>
  Почти весь набор имеющихся в нашем распоряжении донесений командования партизанских 
  отрядов свидетельствует как раз об обратном тому, что утверждали обкомовские 
  «аналитики». Об этом же говорит и проведенная «проверка на месте». Эпизод этой 
  проверки, которая возлагалась на посланного в лес представителя обкома Р. Мустафаева, 
  описал для автора этих строк А.А. Сермуль:<br>
  <br>
  «Для поправления дел (с крымскими татарами. — авт.) в 42-м году прибыл к нам 
  секретарь обкома Мустафаев (он прилетел вместе с Ямпольским). Познакомился с 
  ситуацией и говорит: сейчас мы быстро всех татар распропагандируем и скоро они 
  все у нас будут. Сказано — сделано, неподалеку от леса разведчики заметили пастуха-татарина, 
  который овечек пас. Подобрались к нему, выходят из леса, старик перепугался, 
  а Мустафаев с ним по-татарски начал о чем-то говорить, дед перед ним на колени 
  упал, головой кивает. Мустафаев поговорил с ним и отпустил, сказал, что завтра 
  на этом же месте будет ждать представителей из деревни и пришел в отряд победителем. 
  Ямпольский, однако, засомневался и на следующий день выслал в этот район разведку. 
  Разведчики возвращаются бегом на место встречи и говорят: с двух сторон нас 
  обходят добровольцы. Еле ноги унесли тогда после этой «дипломатической миссии»[130].<br>
  <br>
  Из приведенных «фактов» ошибочного отношения партизан к татарам ни один не может 
  рассматриваться как безупречный. Так, уже приводимый случай, когда группа Зинченко 
  на одной из дорог отобрала продукты у проходящих граждан, противоречит утверждению, 
  что «любого попавшего в лес человека расстреливали». Еще более сомнительным 
  выглядит утверждение, что в «дер. Коуш группа партизан бывшего 4-го района в 
  пьяном виде устроила погром, не разбираясь, кто свои, кто враги». Дело в том, 
  что, по многочисленным свидетельствам, Коуш с самого начала оккупации превратился 
  в опорный антипартизанский пункт оккупантов, и партизаны довольно долго вообще 
  не могли к нему подступиться. Первая операция по захвату Коуша была организована 
  в январе 1942 г. и не принесла результатов. Второй раз партизаны пытались разгромить 
  коушанский гарнизон силами нескольких отрядов двух партизанских районов в отместку 
  за сожжение посёлка Чаир и убийство его жителей 7 февраля, но в ходе многочасового 
  боя также не достигли успеха. Коуш представлял собой укреплённую деревню, появляться 
  в которой, тем более «в пьяном виде», и устраивать «погромы» партизаны попросту 
  не могли.<br>
  <br>
  Мы не склонны представлять действия партизан по отношению к местному населению 
  как «образцовые», фактов всевозможных «ошибок и злоупотреблений» действительно 
  — множество. Однако выбор именно этих, сомнительных во всех отношениях эпизодов, 
  говорит, что обкомовское руководство не ставило своей целью в действительности 
  что-то объяснять. Обком искал не объяснений, а путей исправления ситуации, а 
  для этого годились любые имевшиеся под рукой сведения, пусть даже не имеющие 
  под собой никаких оснований. То же следует сказать и о фактах активной помощи 
  партизанам. В постановлении приводится небольшой список деревень, якобы помогавших 
  партизанам, в котором упомянуты, по крайней мере, две (Бешуй и Ай-Серез), известные 
  как раз активным сотрудничеством с оккупантами. Причём деревня Бешуй несколькими 
  строками выше упоминается именно в этом контексте. Как и Коуш, эта деревня стала 
  одним из главных опорных пунктов оккупантов, и в ней дислоцировался мощный отряд 
  карателей, более того, именно в ней в 1943 г. располагалась разведшкола Абвера, 
  готовившая диверсантов для действий на Кавказе преимущественно из карачаевцев 
  (о чём партизанам не было известно). Деревня Бешуй не могла снабжать продовольствием 
  отряд Селезнёва (что не исключает, конечно, того, что отдельные жители этой 
  деревни могли давать партизанам продукты) и, судя по всему, не делала этого, 
  поскольку именно отряд Селезнёва, как и другие отряды 3-го района, располагавшиеся 
  в районе Бешуя, понес наибольшие потери именно от голода. В то же время обкомовские 
  «аналитики» не упомянули действительно поддерживавших тесные отношения с партизанами 
  сёл, например, дер. Маркур, что свидетельствует о том, что их мало интересовало 
  реальное положение дел. Кроме этого, деревни Айлянма и Чермалык, несмотря на 
  названия, не были «крымскотатарскими», их население было смешанным, что в значительной 
  степени и объясняет их благожелательное отношение к партизанам.<br>
  <br>
  Однако точнее всего о прикладном политическом характере решения говорит порядок 
  определения «виновных», которыми были «назначены» Мокроусов и Мартынов. Им было 
  вменено в вину утверждение о том, что немцам сочувствовало «большинство» татарского 
  населения, а также неверное ориентирование в этом смысле командования отрядов 
  и неправильное информирование обкома. Это почти полностью противоречило тому, 
  что в действительности сообщали в Сочи Мокроусов и Мартынов. В отчёте Крымского 
  обкома, отправленном в Москву 7 июля 1942 г., «татарский вопрос» освещался полностью 
  в духе той информации, которую присылал в Сочи штаб Мокроусова. «Нужно особо 
  отметить отрицательное явление, — сообщали в Москву «крымские товарищи», — отношение 
  известной части татарского населения к партизанскому движению, особенно деревень 
  Коуш, Стиля, Корбек, Куру-Узень, Ускут, Ени-Сала и ряда других (южнобережной 
  части). Эти населенные пункты не только не оказывают помощи партизанам… а наоборот, 
  активно помогают немецко-румынским захватчикам бороться с партизанами. Как правило, 
  сочувствующая часть партизанам или выселена из этих деревень, или уничтожена»[131]. 
  Далее приводились конкретные факты сотрудничества и объяснялись причины, толкнувшие 
  к этому «некоторую часть татарского населения». Как мы видим, здесь нет и намёка 
  на какую-то «огульность», ложные обобщения и т. д. — обком обладал не только 
  объективной, но и взвешенной информацией. Однако в соответствии с советской 
  практикой, если ошибки были признаны — ответственные должны быть названы поимённо, 
  особенно если этого «требовала обстановка», и они были названы, невзирая на 
  справедливость.<br>
  <br>
  Итак, всё говорит о том, что решение обкома «Об ошибках…» было сугубо политическим 
  документом «опережающего действия», а не объективной «справкой» о наличной ситуации 
  (что требует от современных исследователей при использовании его в качестве 
  исторического источника известной осторожности). Обкомовские руководители рассчитывали 
  прежде всего на то, что его содержание будет распространено в Крыму среди татарского 
  населения и станет своеобразным сигналом всем «заблудшим» о том, что на них 
  «не держат зла», что во всём виноваты отстранённые партизанские командиры и 
  что от крымских татар ожидают поворота оружия против своих нынешних покровителей. 
  Одновременно оно должно было настраивать руководство партизанских отрядов на 
  активную работу по расколу коллаборационистских сил, отрыву от них «колеблющейся» 
  части, противопоставлению руководителей и рядовых участников и т. д.[132].<br>
