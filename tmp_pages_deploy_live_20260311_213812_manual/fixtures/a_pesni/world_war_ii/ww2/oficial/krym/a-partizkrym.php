  укреплению партизанских отрядов и дальнейшему развитию партизанского движения 
  в Крыму» от 18 октября 1942 г. И вдруг ровно через месяц действия крымского 
  партизанского руководства объявляются ошибочными, а источник этой информации 
  обвиняется в предвзятом отношении и в неверном информировании обкома. Вот полный 
  текст знаменитого документа:<br>
  <br>
