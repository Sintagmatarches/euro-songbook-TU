  Этот замысел становится очевидным при непредвзятом прочтении документа. Однако 
  на Мокроусова текст постановления должен был произвести шокирующее действие 
  своим неприкрытым цинизмом в отношении к нему лично. Мокроусов воспринял его 
  как удар не только по себе, но и по всему партизанскому движению. В июле 1943 
  г. он доводит до сведения обкома своё письмо, значительная часть которого вновь 
  посвящена «татарскому вопросу». «Когда читаешь протокол обкома ВКП(б) от 18 
  ноября 1942 г., — писал Мокроусов, — волосы дыбом становятся, ибо существо решения 
  сводится к тому, что партизаны шли на поводу у фашистов и своими провокационными 
  действиями против татар восстановили население против партизан»[133]. Далее 
  Мокроусов анализирует постановление (по понятным причинам, несколько сумбурно), 
  давая ответы на различные пункты обкомовских обвинений в свой адрес. Прежде 
  всего это касалось главного «упрёка» — в неправильной оценке поведения «большинства» 
  крымских татар. Мокроусов пытается объяснить обкому, что у него не было реальной 
  возможности получить какие-либо цифры, могущие подтвердить размах коллаборационизма. 
  «Когда мы писали отчёт, мы не выводили процентов» — говорит Мокроусов. Своё 
  заключение о враждебности к партизанам «большинства» татарского населения горной 
  зоны командование движения строило на основе фактов, с которыми столкнулись 
  партизаны лицом к лицу:<br>
  <br>
  «…а) почти во всех татарских деревнях сформированы фашистские отряды из татар, 
  главным образом из дезертиров из Красной Армии..; б) разграбление большинства 
  продбаз татарским населением самостоятельно, а иногда совместно с фашистами, 
  а не только фашистами, как утверждает протокол бюро обкома; в) при всём желании 
  и стремлении партизан установить связь с татарским населением из этого ничего 
  не вышло, (так как) татары к нам на связь не шли, а если мы посылали к ним связных, 
  их выдавали..; г) отряды, сформированные в деревнях: Капсихор, Ускут, Кучук-Узень 
  и других, а также Куйбышевского района за исключением одиночек не пришли в лес 
  партизанить..; д) мы не встречали отрядов (самообороны. — А.М. ), сформированных 
  немцами из других национальностей, правда, старосты и полицейские были во всех 
  деревнях Крыма, но их нельзя смешивать с отрядами»[134].<br>
  <br>
  Мокроусов отвергает обвинение в недостаточной работе с «татарскими массами» 
  и в том, что он «не так» настраивал командование отрядами. «В лесу мы правильно 
  ориентировали товарищей, — заявляет Мокроусов, — в приказах, в наставлениях 
  и в частных беседах, а также в газете «Партизан Крыма», в этой газете мы также 
  разоблачали фашистскую политику в татарском вопросе и призывали татар не верить 
  фашистам и идти к партизанам…»[135]. Он приводит много примеров агитационной 
  работы партизан в татарской среде, которую, несмотря ни на что, вели партизаны.<br>
  <br>
  Далее отставленный командующий разбирает другие пункты постановления, останавливаясь, 
  в частности, на «фактах» грабежа партизанами татарских крестьян, якобы имевших 
  место запретов передвигаться по дорогам и «бессудных» расстрелов «за мародерство». 
  Приводя пункт постановления, утверждавший, что грабёж баз расценивался партизанами 
  как мародёрство со стороны местного населения и «любого попавшего в лес гражданина 
  расстреливали», Мокроусов восклицает: «Это неправда, расстреливали тех, независимо 
  от национальности, кого ловили на месте разграбления баз». При этом командующий 
  не исключил возможности того, что «некоторые уполномоченные ОО, через которых 
  проходили все, кого задерживали в лесу, где-нибудь и допустили ошибку». Вообще 
  же, отметая обвинения обкома, Мокроусов всё же признал: «Беда наша заключалась 
  в том, что мы не смогли проникнуть в нутро татарского населения, не знали, что 
  делается в гуще, а судили по внешним признакам»[136]. Экскомандующий намекает, 
  что ему понятны мотивы решения обкома: «… бюро обкома осуждает как неправильное 
  и политически вредное утверждение о враждебном отношении большинства татарского 
  населения к партизанам. Возможно, что здесь обком прав, но товарищи, приехавшие 
  из Крыма определяют только намечающийся перелом в отношении татарского населения 
  к фашистам, вызванный тем, что фашисты начали затрагивать коренные интересы 
  татар, обкладывать налогами… и т. д…. Но в то же время как сообщает тов. Ямпольский 
  (один из секретарей обкома, направленный осенью 1942 г. в Крым. — Авт.), в татарских 
  деревнях партизан встречают по-прежнему огнём». Он завершает своё письмо утверждением 
  о том, что Решение обкома «бросает на нас (т.е. партизан) тяжкое обвинение, 
  низводящее нас до степени проводников фашистской политики», и требует либо привести 
  в подтверждение этого «веские факты», либо отказаться от него.<br>
  <br>
  Для обкомовского руководства реальная ситуация в крымскотатарской среде также 
  не представлялась вполне ясной. К концу 1942 г. или даже позднее по заданию 
  одного из секретарей обкома Б. Лещинера составляется документ под названием 
  «Выборочный материал по татарскому вопросу», представляющий собой выдержки из 
  различных партизанских донесений и докладов, касающихся ситуации в крымскотатарской 
  среде с ноября 1941 г. по декабрь 1942 г. Весьма характерно, что этот документ 
  составляется не до, а после принятия Решения 18 ноября 1942 г., что также говорит 
  о том, что постановление обкома в соответствии с известной марксистской формулой 
  не столько отражало реальность, сколько должно было изменить её. Судя по подписи, 
  «Выборочный материал» составлял Р. Мустафаев; поскольку документ не подлежал 
  огласке, автор в целом даёт довольно правдоподобную картину реальной ситуации, 
  которая во многих ключевых моментах расходится с формулировками Решения обкома 
  (в основу настоящей работы положены материалы именно этого документа). Тем не 
  менее, это не повлияло и, добавим, не могло повлиять на изменение позиции обкома 
  в отношении оценки действий Мокроусова и «татарского фактора». Свою позицию 
  обком вновь подтвердил в специальном «Заключении по заявлению тов. Мокроусова 
  на имя секретаря Крымского обкома ВКП(б) тов. Булатова В.С.», датированном 15 
  июля 1942 г. и подписанном секретарями обкома Лещинером и Сейфулаевым. Отвечая 
  на возражения Мокроусова, авторы заключения констатировали, что «попытки тов. 
  Мокроусова опровергнуть этот пункт показывают, что он не только в бытность командующим, 
  но и сейчас, уже после решения бюро обкома, не понимает смысла и политического 
  значения принятого бюро обкома решения по татарскому вопросу». Далее следует 
  развёрнутое повторение обкомовских обвинений Мокроусову, которые можно свести 
  к одному: командующий слишком доверился «фактам», забыв, что иногда они могут 
  противоречить «идее». Главная же «идея» обкома заключалась в том, что, несмотря 
  на весь размах сотрудничества с немцами, «в глубине» татары всё равно остаются 
  «советскими людьми», надо только «реанимировать» этот глубоко скрытый «патриотизм» 
  и повернуть татар против их нынешних покровителей.<br>
