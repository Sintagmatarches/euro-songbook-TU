  Ситуация с продовольственными базами уже в январе 1942 г. оценивалась партизанским 
  командованием как катастрофическая. В результате декабрьских боёв в лесах Госзаповедника, 
  где дислоцировались отряды 3-го и 4-го районов и Центральный штаб, было уничтожено, 
  согласно «Докладной записке» Мокроусова, 60—80% всех баз.[42] (Следует добавить 
  также потерю в декабрьских боях большей части денежных средств отрядов, что 
  сделало невозможным компенсировать деньгами продовольствие, которое партизаны 
  должны были добывать у населения). В другом донесении Мокроусов сообщает, что 
  к январю 1942 г. все отряды 3-го и 4-го районов, кроме Евпаторийского и Ак-Мечетского, 
  баз не имели[43]. Не многим лучше было положение и в других районах: 5-й район 
  лишился своих баз уже в ноябре 1941 г. (согласно сообщению бывшего начштаба 
  И.К. Сметанина, эти базы выдал предатель Ибрагимов, который как раз и занимался 
  их закладкой[44]), здесь положение было самым тяжелым; отряды 1-го района потеряли 
  свои базы в феврале, будучи вытесненными из зоны своих действий во 2-й район. 
  Относительно более благоприятная ситуация с базами сложилась в отрядах 2-го 
  района отчасти благодаря тому, что закладкой там занимался опытный человек И.Г. 
  Генов, но главным образом, по-видимому, потому, что население окрестных деревень 
  этого района было неоднородным в национальном отношении и вследствие этого не 
  подвержено в такой степени немецкой агитации, как в деревнях, окружавших отряды 
  3-го, 4-го и 5-го районов. В январе 1942 г. отряды 2-го района заняли ряд населённых 
  пунктов и до апреля обеспечивали себя продуктами. Однако и отрядов здесь было 
  больше всего, к тому же второй район должен был делиться своими продуктами с 
  голодающими; сюда же вскоре передислоцировался и штаб во главе с Мокроусовым, 
  так что к апрелю 1942 г. забазированных продуктов практически не осталось и 
  здесь.<br>
  <br>
  Это привело к возникновению и распространению в большинстве отрядов голода, 
  который стал настоящим кошмаром для партизан. Вначале люди пробавлялись охотой 
  на диких животных, но они были очень быстро выбиты, тогда в ход пошли корешки, 
  древесная кора, мох, шкуры и останки ранее павшего скота, которые выкапывали 
  из-под снега; бойцы варили и ели кожаные постолы, ремни и т.д.; хлеб в виде 
  сухарей — основной и элементарный продукт питания — стал настоящей роскошью, 
  часто партизаны не видели его неделями, то же можно сказать и о соли. Начались 
  случаи смерти на почве истощения, которые к весне 1942 г. приобрели массовый 
  характер. Сегодня можно только приблизительно оценить размеры этой трагедии, 
  данные разных отчётов существенно разнятся, но одинаково поражают. Согласно 
  отчету о боевых действиях партизан Крыма, за 11 месяцев 1942 г. (до декабря, 
  т.е. без двух месяцев 1941 г.) потери партизан их командованием оценивались 
  в следующих цифрах: убитыми — 398 человек, пропавшими без вести — 473, умершими 
  от голода — 473 человека[45], т.е. умерло больше, чем было убито в боях. Сходная 
  картина вырисовывается и из отчёта И. Вергасова, согласно которому к июлю в 
  отрядах 4-го и 5-го районов умерло более 150 человек[46] — это также больше, 
  чем отряды этих районов потеряли убитыми в боях, таковых было 120 человек. По 
  другим данным, только зимой 1942 г. в отрядах 3-го, 4-го, 5-го районов умерло 
  от голода до 400 чел[47]. Эта цифра, судя по всему, ближе к истине. В некоторые 
  дни этого кошмарного времени смертность в отрядах 3-го района, например, достигала 
  30 — 40 человек в день. Это заставило командира партизанских отрядов Крыма полковника 
  М.Т. Лобова (он сменил Мокроусова в июле 1942 г.) написать в отчете о результатах 
  боевых действий, что «в 3-м районе дошло до катастрофы. Там голодной смертью 
  умерло 362 человека, и в 11-ти случаях были факты людоедства»[48]. Следует заметить, 
  что донесение Лобова — единственный источник, который называет такое количество 
  фактов людоедства (под людоедством здесь нужно понимать использование в пищу 
  частей трупов убитых в боях или умерших людей, т.е. трупоедство), в других документах 
  фигурирует только один эпизод, но он сам по себе достаточно красноречиво иллюстрирует 
  ужасающую картину голода в партизанских отрядах. О нём весьма подробно рассказал 
  автору этих строк боец-разведчик 3-го симферопольского отряда, впоследствии 
  комиссар 6-го отряда А.А. Сермуль в 2004 г.: <br>
  <br>
  «В 42-м году, зимой, немцы с добровольцами крепко зажали отряд (нашей группы 
  там в это время не было, мы находились при Северском в штабе района) в т.н. 
  «Мокром лагере». Был тяжелейший бой, в результате которого было убито 10—12 
  человек, отряд окружили, и он пробивался с боем. Немцы захватили лагерь, в том 
  числе санитарный шалаш (мы жили тогда не в землянках, а строили шалаши), убили 
  раненых, шалаши подожгли и трупы туда побросали. Во время боя от отряда отбилась 
  группа — 4 человека, фамилии двоих я помню, потому что их лично знал, один из 
  них — старший политрук по званию, до войны был комиссаром подводной лодки, а 
  второй боец из нашей разведгруппы. Они трое суток блукали по лесу, искали своих, 
  пришли на это погорелое место и от запаха горелого мяса из-за голода просто 
  обезумели. Стали ножами резать эти трупы обгоревшие и есть. Может быть, об этом 
  бы никто не узнал, но они еще с собой в отряд части этих трупов притащили. Ну, 
  когда об этом стало известно, Макаров с Чукиным и Шагибовым — начальником разведки 
  — приняли решение расстрелять их. Больше таких случаев, насколько мне известно, 
  не было, хотя от голода умирало очень много людей[49]».<br>
