  «Решение бюро обкома по татарскому вопросу, — говорилось в Заключении, — сыграло 
  исключительно положительную роль, оно помогло правильно понять татарский вопрос, 
  разоблачило подлую демагогическую политику немецких оккупантов в отношении татар, 
  нацелило партизанские отряды на организацию работы среди татарского населения. 
  Еще работы с татарским населением, — констатировалось здесь же, — непочатый 
  край. Решение бюро обкома… ещё не выполнено до конца. Много еще предстоит сделать 
  для разоблачения фашистской политики по отношению к крымским татарам, ещё много 
  надо сделать для того, чтобы поднять весь татарский народ на борьбу против фашистских 
  поработителей. Но для этого необходимо, прежде всего, правильное партийное понимание 
  всей сложности татарского вопроса в Крыму, а ненужная полемика по уже принятому 
  партийному решению, которую затеял тов. Мокроусов, только мешает этому важнейшему 
  делу»[137].<br>
  <br>
  В своём письме Мокроусов допустил еще одну ошибку, несравнимо большую, чем непризнание 
  своей вины: в сердцах отставленный командующий посмел напомнить обкомовским 
  товарищам и об их ответственности за происходящее как главных представителей 
  Советской власти: «В 1920 г. татары горной части Крыма не прошли 21-го года 
  советского воспитания, однако они находили в лесу партизан, поддерживали их 
  и не считали бандитами…»[138]. Это было уже слишком. «Выходит, по Мокроусову, 
  — комментируют этот пассаж Лещинер и Сейфуллаев, — что советское воспитание 
  испортило татар и вооружило их против Советской власти? Необходимо, чтобы тов. 
  Мокроусов объяснил свою мысль…»[139]. Мокроусов был еще раз вызван на бюро обкома, 
  где ему в последний раз объяснили, как надо правильно понимать политику партии. 
  Объяснили таким образом, что бывший командующий написал новое покаянное письмо 
  обкому, где, наконец, признал все свои прегрешения и попросил изъять прежнее 
  послание из дела (что, к счастью, не было сделано). После этого обком решил 
  отправить опального партизанского командира от себя подальше и, по некоторым 
  сведениям, Мокроусов был до конца войны «сослан» куда-то в Поволжский военный 
  округ комплектовать резервные части для отправки на фронт. Его «реабилитация» 
  состоялась много позже, уже в 60-х годах.<br>
  <br>
  Политика обкома по отношению к крымским татарам в конце концов начала давать 
  свои плоды. Если летом 1943 г. «работы с татарским населением» был «еще не початый 
  край», то уже осенью этого года в партизанский лес стали приходить первые перебежчики 
  из добровольческих батальонов. Безусловно, ключевую роль в этом играло не Решение 
  обкома от 18 ноября 1942 г., а изменение ситуации на советско-германском фронте. 
  После Сталинграда, и в особенности после Курской битвы, многим стало ясно, что 
  поражение Германии неизбежно и следует искать пути возвращения назад. Однако 
  и значение Решения в этом процессе также не следует приуменьшать. Оно весьма 
  способствовало разложению коллаборационистских формирований, отрыву крымскотатарских 
  масс от руководства мусульманских комитетов и способствовало изменению отношения 
  населения крымскотатарских деревень к партизанам от открыто враждебного к нейтральному 
  и даже к сочувственному. С осени 1943 г. до апреля 1944 г. в партизанские отряды 
  перешли около 600 крымских татар, большей частью из добровольческих батальонов, 
  многие из них храбро воевали на этапе освобождения Крыма от немецко-румынских 
  войск. Для усиления этого процесса обком и созданный летом 1943 г. Крымский 
  штаб партизанского движения направил в лес целый ряд ответственных работников 
  из крымских татар. <br>
