  <br>
  В отличие от существовавших параллельно отрядов самообороны, «привязанных» к 
  своим деревням, роты можно было использовать гораздо шире. Служащие «татарских 
  рот» имели статус, равный статусу «военнослужащих Вермахта», носили немецкую 
  военную форму, получали жалование и специальные земельные наделы. Несмотря на 
  то, что крымские татары составляли основную часть военнослужащих этих подразделений 
  и сами они официально назывались «татарскими», иногда в их создании принимали 
  активное участие и нетатары. Так, известно, что созданием таких частей в Карасубазаре 
  и окрестностях занимался некто Яблонский[64], активную роль в создании коушанского 
  отряда играл бывший шахтёр бешуйских угольных копей Лазарев,[65] а евпаторийское 
  подразделение создавал знаменитый впоследствии деятель РОА В. Мальцев. То обстоятельство, 
  что организованные подразделения подобного рода не создавались из представителей 
  каких-либо других национальностей Крыма, способствовало росту неприязни к крымскотатарскому 
  населению в целом со стороны партизан.<br>
  <br>
  Существенен вопрос о реальном количестве тех, кто помогал немцам с оружием в 
  руках. Вопреки некоторым утверждениям количественный состав можно достаточно 
  четко определить на основании немецких источников. Немецкий документ «Формирование 
  татарских и кавказских воинских частей в пределах штаба 11-й армии», направленный 
  в феврале 1942 г. в главное командование сухопутных сил и командующему тыловыми 
  сухопутными войсками южных областей, сообщает о том, что в результате вербовки 
  в распоряжение военного командования поступило всего около 10 000 добровольцев, 
  кроме этого, «по данным татарского комитета, старосты деревень организовали 
  еще около 4000 человек для борьбы с партизанами. Кроме того, наготове было около 
  5000 добровольцев для пополнения сформированных воин¬ских частей». Таким образом, 
  автор документа делал вывод: «при численности населения около 200 000 человек 
  татары выделили в распоряжение нашей армии около 20 000 человек. Если учесть, 
  что около 10 000 человек были призваны в Красную Армию, то можно считать: все 
  боеспособные татары полностью учтены»[66]. Таким образом, к февралю 1942 г. 
  вооруженный контингент крымских татар состоял из тех, кто непосредственно находился 
  в частях 11-й армии и ротах СД (10 316 человек), участников иррегулярных отрядов 
  самообороны, числившихся «на службе германского вермахта» (около 4000 человек) 
  и около 5000 резервистов, то есть всего несколько более 19 тыс. человек. При 
  этом партизан в это время находилось в лесу чуть больше 3 тыс. человек.<br>
  <br>
  С января 1942 г. партизанам противостояла по существу новая организованная сила, 
  отчет в этом отдавали себе и сами партизаны. «Вооруженные татары куда опаснее 
  немцев… и румын»,[67] — доносил на Большую землю командир 2-го района И.Г. Генов, 
  хотя известные документы показывают, что партизаны, похоже, не делали различия 
  между «самообороной» и регулярными «татарскими ротами», как правило, они всех 
  называли «добровольцами» или «самооборонцами». Если в ноябре-декабре 1941 г. 
  участие самооборонцев в боях с партизанами было эпизодиче¬ским, то, по крайней 
  мере, со второй половины января по июль 1942 г. именно они становятся ключевой 
  антипартизанской силой оккупационных властей, поскольку почти все боеспособные 
  немецкие и даже румынские части были в этот период задействованы против Севастополя 
  и на вновь образовавшемся на Керченском полуострове Крымском фронте. Кроме этого 
  новые подразделения принимали участие в разгроме Судакского десанта и понесли 
  при этом значительные потери — до 400 человек убитыми и ранеными. Партизанские 
  донесения этого периода просто пестрят сведениями о боях, в которых самооборонцы 
  принимают участие как наряду с немецкими и румынскими частями, так зачастую 
  и самостоятельно.<br>
  <br>
  Комиссар 3-го района Никаноров (апрель 1942 г.):<br>
  <br>
  «Состав добровольцев-полицейских из татарского населения растёт и за последнее 
  время особенно активизируется по борьбе с партизанами»;<br>
  <br>
  Замнач. О.О. Попов (июнь 1942 г.):<br>
  <br>
  «При каждом нападении немецких войск на партизанские отряды, как правило, татары 
  принимают участие не только как проводники, но целыми группами по 60 — 100 человек, 
  а в некоторых случаях сами татары производят нападения на партизанские отряды»;<br>
  <br>
  Начштаба партизанского движения Крыма Сметанин (июль 1942 г.):<br>
  <br>
  «Как правило, добровольцы, преимущественно татары, немцами используются против 
  партизан, причём их посылают вперёд, а сами двигаются сзади… Вообще же татары-добровольцы 
  активно борются против партизан и жестоко с ними расправляются, если захватывают 
  их в плен»[68].<br>
  <br>
  Еще одной ключевой миссией «самообороны» становится расправа над немногочисленными 
  селениями, поддерживавшими партизан. Зимой и весной 1942 г. самооборонцы принимали 
  активное участие в карательных экспедициях против таких сёл, а часто действовали 
  самостоятельно. Согласно докладу командования 2-го района (Н. Селихов, Бускадзе): 
  «Некоторые населённые пункты, как Айлянма, Чермалык, (где) население оставалось 
  верным советской власти, татарами были сожжены, а часть жителей, включая детей, 
  — были расстреляны»[69]. То же самое происходило и в лесах Госзаповедника, где 
  партизан поддерживали посёлок шахтёров Бешуйских угольных копей Чаир и греческая 
  деревня Лаки. Здесь в обоих случаях отличился отряд самооборонцев д. Коуш, на 
  основе которого в январе 1942 г. была сформирована 9-я «татарская рота»: «Когда 
  татары села Коуш, — доносил замначальника особого отдела центрального штаба 
  Попов, — узнали о связи жителей пос. Чаир с партизанами, то они вместе с немцами 
  пришли в посёлок, сожгли все жилые и нежилые постройки, разграбили всё имущество, 
  а мужчин 18 человек и одного ребёнка в возрасте 3-х лет расстреляли…»[70]. Здесь 
  проявился характерный для самооборонцев «почерк» в расправах с захваченным противником 
  — сожжение. В одном из домов Чаира на излечении находились два тяжелораненых 
  партизана красноармейского отряда, которых каратели запретили выносить, и они 
  погибли в огне. Согласно воспоминаниям очевидцев, особую роль в трагедии Чаира 
  сыграл коушский коллаборационист Смаил Ягъя[71]. Вскоре настал черед деревни 
  Лаки. Это село, населённое преимущественно греками, активно помогало бахчисарайскому 
  партизанскому отряду, которым командовал М. Македонский — тоже грек. В значительной 
  степени именно благодаря этой помощи бахчисарайский отряд имел наименьшие потери 
  от голода и сохранял высокую боеспособность. 24 марта 1942 г. в село явились 
  коушанские добровольцы и немцы, которые, согласно записи в дневнике Бахчисарайского 
  партизанского отряда: «сожгли дотла всю деревню, разрушили школу, клуб, здание 
  сельсовета, магазин, жилые постройки колхозников и даже церковь, а 16 женщин, 
  стариков и детей, в том числе Елену Гавалло с её 8-месячным ребёнком, расстреляли. 
  Трупы расстрелянных бросили в один из погребов, облили бензином и сожгли»[72]. 
  Таким образом, к весне 1942 г., благодаря активной помощи вооруженных коллаборационистов, 
  небольшая местная база партизанских отрядов была полностью подорвана.<br>
