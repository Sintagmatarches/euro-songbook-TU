  Конвоями дело не ограничилось. В полосе действия 11-й армии довольно быстро 
  возникла целая система антипартизанских мероприятий, которая затем была использована 
  во всей немецкой армии на Восточном фронте. Известные специалисты по партизанским 
  действиям Ч. Диксон и О. Гейльбрунн считали, что борьба с партизанами была лучше 
  всего организована именно 11-й армией, действия которой «представляют в этом 
  отношении прекрасный образец[11]», но даже эта система, добавим мы, не устранила 
  угрозу со стороны партизан на всём протяжении немецкой оккупации Крыма. Первоначально 
  задачи борьбы с партизанами были возложены Манштейном на начальника его контрразведки, 
  однако командующий, «очевидно, не был удовлетворён … предпринятыми его войсками 
  мерами по ликвидации угрозы со стороны партизан»[12]. Уже 29 ноября 1941 г. 
  Манштейн отдаёт приказ по армии «Об организации и методах борьбы с партизанами», 
  которым создаётся специальный штаб по организации антипартизанских действий, 
  под руководством майора генштаба Стефанус (в другой транскрипции — Штефанус). 
  Начальник штаба получил весьма широкие полномочия, а также значительное количество 
  войск в своё распоряжение для решения поставленных перед штабом задач. В докладе 
  об организации борьбы с партизанами, отправленном Манштейном 5 декабря 1941 
  г. главнокомандующему группой армий «Юг», перечислялись эти войска. <br>
