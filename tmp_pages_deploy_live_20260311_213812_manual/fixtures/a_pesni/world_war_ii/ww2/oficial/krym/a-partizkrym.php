  Все эти мероприятия заслуживают отдельного и подробного рассмотрения. Что же 
  касается дальнейшей судьбы исторического Решения, то оно стало объектом специального 
  рассмотрения еще раз. В 1944 г. бывший комиссар партизанских отрядов Крыма С.В. 
  Мартынов обратился в бюро Крымского обкома ВКП(б) с заявлением об отмене предъявленных 
  ему в Решении от 18 ноября 1942 г. обвинений. Рассмотрев 21 сентября 1944 г. 
  вновь вопрос, по поводу которого было уже так много «сломано копий», бюро обкома 
  на этот раз установило следующее: «Из имеющихся материалов, — говорится в Протоколе 
  заседания, — видно, что эти сообщения (Мокроусова и Мартынова о враждебном отношении 
  к партизанам крымскотатарского населения горно-лесной зоны. — Авт.) были не 
  личными предположениями, а основаны на материалах, которые поступали от руководителей 
  партизанских отрядов, и изобиловали фактами, подтверждавшими враждебное отношение 
  татар к партизанам и переход их на сторону немецко-румынских оккупантов (докладные 
  записки за 1942 год руководителей районов (соединений), партизанских отрядов, 
  находящиеся в делах Крымского штаба партизанского движения)»[140], в соответствии 
  с этим выводом бюро обкома постановило: 1. Предъявленные Мокроусову и Мартынову 
  обвинения отменить и 2. Признать прежнюю информацию этих руководителей правильной. 
  К сожалению, это новое решение было вынесено уже после депортации крымских татар, 
  а также представителей других крымских этнических групп с территории полуострова 
  и под влиянием этого акта, поэтому оно не может рассматриваться в качестве окончательного 
  и безупречного вердикта. В частности, оно снова подтвердило якобы «татарофобскую» 
  позицию Мокроусова с той лишь разницей, что теперь одобрило те «взгляды», которых 
  Мокроусов в общем-то никогда не придерживался.<br>
