  Обкому нужно было оценить ситуацию, выявить виновных и наметить конкретные меры 
  по исправлению положения дел. Речь, по существу, шла не только о выработке определенной 
  и активной политики по отношению к крымским татарам, которой до этого у обкома 
  попросту не было, но и о фактической самореабилитации обкома, на котором в полной 
  мере лежала ответственность за положение дел в подведомственном регионе. И обком 
  решил эту проблему, правда, способом, породившим многочисленные вопросы, на 
  которые до недавнего времени ещё не было адекватных ответов.<br>
