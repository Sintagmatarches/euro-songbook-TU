  В апреле 1942 г. накануне предполагавшегося наступления Крымского фронта с Керченского 
  полуострова фронтовое командование наконец смогло поддержать партизан заброской 
  продуктов по воздуху. Смертность удалось приостановить, хотя голод не прекратился, 
  но после разгрома Крымского фронта в мае и особенно после падения Севастополя 
  и переноса боевых действий на Северный Кавказ, когда существовавшие на Кубани 
  аэродромы были эвакуированы еще дальше на восток, голод в партизанских отрядах 
  разразился вновь. Уже в августе 1942 г. снова началась смертность на почве истощения, 
  унёсшая десятки жизней. С этих пор практически до осени 1943 г. голод был постоянным 
  спутником партизанского бытия. Конечно, вызывался он во многом отсутствием постоянного 
  снабжения с Большой земли, но в сознании партизан был накрепко связан с разгромом 
  продовольственных баз в конце 1941 — начале 1942 гг. Так, начальник медслужбы 
  Крымского штаба партизанского движения подполковник П. Михайленко в своём отчёте 
  об итогах работы медицинской службы в партизанских отрядах прямо заявляла: «В 
  условиях крымской партизанской борьбы не было бы смертей от голода, если бы 
  не измена крымских татар (разграбление продовольственных баз и т. д.)».<br>
  <br>
  <strong>4</strong><br>
  <br>
  Вместе с дезертирством и разграблением продовольственных баз партизанам пришлось 
  столкнуться и с фактами прямого сотрудничества представителей местного татарского 
  населения с оккупантами. Проводник-татарин, идущий во главе немецкого или румынского 
  подразделения к расположению отрядов, — вот картинка, без которой не обходятся 
  ни одни партизанские воспоминания о первых днях борьбы в Крыму. «Они, — говорит 
  о крымских татарах один из немецких документов, — оказали неоценимую помощь 
  как разведчики, проводники и знатоки страны»[50]. Справедливости ради нужно 
  отметить, что проводниками служили не только татары, среди них попадаются и 
  славянские фамилии (Налимский, Лазарев, Коневец). И всё же татар было больше 
  — их было больше в национальной структуре предгорного населения. Если бы сёла 
  были греческие, проводники наверняка тоже были бы греками, если бы белорусские, 
  то белорусами и т. д., но это не меняет основного факта — во главе немецких 
  отрядов партизаны видели главным образом татар.<br>
  <br>
  <img src="a-partizkrym8.jpg" width="198" height="273"><br>
  <br>
  «В нападении на партизанские отряды, — докладывал замначальника особого отдела 
  Центрального штаба Попов, — большую роль всегда играют местные татары, которые, 
  хорошо зная лес, дороги и тропы, являются проводниками и всегда приводят с той 
  стороны, откуда их меньше всего ожидали»[51]. Проводники сыграли ключевую роль 
  в разгроме немцами подразделений 48-й кавалерийской дивизии, которая прикрывала 
  отход от Перекопа основных частей 51-й и Приморской армий. Последний бой разрозненные 
  остатки дивизии дали в окрестностях Алушты 6 ноября 1941 г. «Целый день 6 ноября, 
  — сообщал в своём рапорте военком дивизии, — мы дрались и скакали от рубежа 
  к рубежу. Последний рубеж в районе дер. Куру-Узень мы удерживали до 16.00. К 
  этому времени татары-предатели из дер. Казанлы вывели к нам в тыл автоматчиков 
  и создалось такое положение, при котором 68-й кавполк, прикрывавший дорогу Ускут-Карасубазар, 
  оказался совершенно от нас отрезанным. Остальные части — 62-й, 71-й и 147-й 
  к.п. и др. оказались в тактическом окружении. Единственный выход вёл в лес через 
  ущелье по реке Суват на г. Демерджи»[52]. Обращает на себя внимание то, что 
  современники почти всегда говорят, будто помощь, которую получали оккупанты, 
  предлагалась добровольно. Так, Э. Юсуфов в своем донесении сообщал: «При первых 
  появлениях румын, главным образом в лесу для нападения на партизан, обеспечены 
  были добровольные проводники. Со стороны дер. Суук-Су Тат Мустафа, дер. Ворон 
  Караев Умер, дер. Ай-Серез Рамазан Садла, Судака Коневец Иван быв. председатель 
  Ленинского сельсовета. Из дер. Кутлак долгое время работали два разведчика, 
  которые появлялись в лесу якобы в поисках лошадей. При втором нападении на Судакский 
  отряд 28 декабря 1941 г. один только Ай-Серес и Ворон обеспечили немцев 17-ю 
  проводниками»[53]. Здесь примечательно то, что Юсуфов отмечает еще одну форму 
  сотрудничества с оккупантами — это разведка дислокации партизанских отрядов, 
  которую осуществляли местные жители по заданию оккупантов под видом пастухов, 
  собирателей ягод, заготовителей леса и т. д. Это заставляло партизан подозревать 
  в каждом крымском татарине, появившемся в лесу, вражеского агента. Информирование 
  о партизанских отрядах было поставлено настолько широко, что уже менее чем через 
  месяц после занятия Крыма немецкий штаб по борьбе с партизанами был прекрасно 
  осведомлён о местонахождении центрального штаба Мокроусова и его составе.<br>
  <br>
  Еще одним проявлением враждебности, что позволяют выявить архивные документы, 
  были, как это ни прискорбно, нападения на советских военнослужащих, отставших 
  от своих частей в ходе отступления, на разведчиков и десантников, забрасывавшихся 
  в Крым в ходе Керченско-Феодосийской десантной операции. Наряду с теми жителями 
  татарских деревень, которые оказывали помощь выходившим из окружения, заблудившимся 
  красноармейцам в ноябре 1941 г., были и такие, кто совершал убийства советских 
  военнослужащих с последующей передачей доказательств содеянного немецким властям. 
  Бывший боец 3-го симферопольского, впоследствии командир 6-го отряда Н.И. Дементьев 
  рассказывал автору этих строк о том, что во время отступления к Севастополю 
  группа морских пехотинцев, в которую он входил, получала продовольствие и сведения 
  об обстановке у местных жителей — крымских татар, в то же время он приводит 
  факт убийства двух краснофлотцев в одной из предгорных крымскотатарских деревень, 
  когда зашедшие отдохнуть моряки были убиты во сне, а их головы с надетыми бескозырками 
  были преподнесены на подносе командиру вошедшей в село немецкой воинской части[54]. 
  Если в ноябре 1941 г. такие случаи были сравнительно редки, то в ходе Керченско-Феодосийской 
  десантной операции они становятся широко распространенным явлением. Известно 
  о гибели нескольких разведгрупп, пришедших в сёла и убитых местными коллаборационистами. 
  В некоторых случаях их просто сжигали в домах, где они останавливались для отдыха. 
  Об этом сообщал, в частности, Э. Юсуфов: «В Шелене жгли парашютистов, в Вороне 
  жгли в январе 12 красноармейцев из морского десанта, когда жгли этих красноармейцев, 
  участвовали люди из дер. Ворон, Шелен, Капсихор, Ай-Серез. Население этих деревень 
  при встрече с десантниками и партизанами в лесу сразу заявляло, чтобы те сдавались 
  в плен. Высадку десанта в Новый Свет заметили кутлакские люди, которые заявили 
  об этом в немецкий штаб и в уничтожении этого десанта принимали самое активное 
  участие. Были случаи, когда пойманных красноармейцев раздевали догола, а в Таракташе 
  один татарин убил краснофлотца и одежду взял себе…»[55]. Сохранилось донесение 
  о том, как происходила акция по уничтожению разведгруппы сержанта Юргенсона 
  в дер. Ворон:<br>
  <br>
  «9 января 1942 г. в районе г. Старый Крым отдельным парашютным батальоном Крымского 
  фронта была выброшена спецгруппа парашютистов под командой сержанта К.П. Юргенсона. 
  Грузовые парашюты унесло за г. Агармыш и группа осталась без радиостанции, продовольствия 
  и боеприпасов. 10 суток 12 парашютистов пытались найти партизан или перейти 
  линию фронта, но выполнить это не удалось. «НЗ» закончился и Юргенсон решил 
  спуститься к морю достать продуктов. Зашли (мокрые, голодные, измученные) в 
  крайний от гор дом в дер. Ворон и попросили продать еду. Хозяин пригласил в 
  дом обогреться, а дочерей отправил за полицией. Дом был окружен самооборонцами 
  из села. Послали в Кутлак за немцами, но те ехать отказались: «Делайте с ними 
  что хотите». К вечеру в Вороне собралось до 200 татар из Ай-Сереза, Шелена. 
  Десантники отстреливались. Тогда татары решили сжечь их живьём. К татарам прибыла 
  подмога ещё из Капсихора. Общиной решили собрать хозяину дома деньги на строительство 
  нового дома, собрали в сёлах керосин, мазут, солому и дом сожгли. Все десантники 
  сгорели или задохнулись в дыму, отстреливаясь до последнего патрона. Погибли: 
  мл. с-т К.П. Юргенсон, рядовые краснармейцы: А.В. Зайцев, Н.И. Демкин, М.Г. 
  Кохаберия, Л.И. Нетронькин, Н.Х. Трегулов, А.В. Богомолов, В.С. Быков, А.К. 
  Борисов, Б.Д. Адигиезалов, К.А. Колясников и Г.Г. Казарьян»[56].<br>
  <br>
  <strong>5</strong><br>
  <br>
  Однако главной проблемой для партизанского и армейского руководства явилось 
  появление в целом ряде крымскотатарских деревень организованных немцам вооруженных 
  подразделений.[57] «В борьбе с партизанами, — свидетельствовал в 1945 г. Манштейн, 
  — у нас было много помощников. В горах Яйла в Крыму есть… недоступные места, 
  где скрывались партизаны. Но мы не могли до них добраться, так как у нас не 
  было подготовленных для этого войск. Единственное, что мы могли предпринять, 
  это попытаться заморить партизан голодом, не давая им возможности совершать 
  налёты на татарские деревни и пополнять свои запасы продовольствия. С этой целью 
  мы вооружили татар, чтобы с нашей точки зрения сделать их деревни надёжными. 
  В этом нам помогали органы СД. Татары действовали вместе с нами при поисках 
  складов продовольствия партизан. Мы были вынуждены так поступать, ибо у нас 
  не было свободных немецких войск»[58]. А.А. Сермуль и Н.И. Дементьев утверждали, 
  что с «самооборонцами» из числа крымских татар им пришлось столкнуться почти 
  сразу же после начала партизанских действий, т.е. уже в ноябре 1941 г.<br>
  <br>
  Отряды самообороны, или, как они официально именовались, «Отряды вспомогательной 
  службы» начали формироваться согласно приказу начальника штаба 11-й армии «О 
  самообороне населения против партизан». В приказе, в частности, определялось: 
  «1. Борьба против партизан должна предусматривать уничтожение продовольственных 
  складов и складов боеприпасов. В этих случаях партизаны будут вынуждены получать 
  помощь в населённых пунктах, зачастую применяя силу. Население вынуждено будет 
  обороняться, в том числе и с помощью немецких войск, находящихся в этих районах. 
  В населённых пунктах, далеко расположенных от немецких войск, нужно организовать 
  самооборону. 2. В борьбе с партизанами хорошо зарекомендовали себя татары и 
  мусульмане, особенно в горах, сообщая о партизанах и помогая их выследить. Из 
  этих слоёв населения необходимо привлекать людей для дальнейшего сотрудничества 
  и особенно активного сопротивления партизанам в получении ими продовольствия»[59]. 
  Формирование таких отрядов возлагалось на командование армейских корпусов и 
  дивизий, служба в этих подразделениях считалась почётной и не оплачивалась, 
  служащие в них носили гражданскую одежду или советскую военную форму без знаков 
  различия и белую повязку с надписью «на службе германского вермахта» на рукаве. 
  Командир и комиссар 3-го района Г. Северский и Никаноров в 1942 г. сообщали 
  следующее: «С первых дней оккупации немецкими войсками Крыма резко оживили свою 
  деятельность буржуазно-националистические и уголовные элементы, особенно в населённых 
  пунктах с татарским населением… Эти лица принимали активное участие в организации 
  добровольческих отрядов местной самообороны, в организации карательных отрядов 
  для борьбы с партизанами. Особенно интенсивно эта работа проводилась в прилесных 
  и горных районах Крыма. К концу декабря (1941 г. — Авт.) им удалось завербовать 
  в эти группы, отряды до 14 тыс. чел. из татарского населения, главным образом 
  из мужчин призывного возраста»[60]. Очевидно, что уже в 1941-м г. добровольческие 
  подразделения стали серьезным фактором в действиях оккупантов против партизан. 
  Наиболее активное содействие оккупантам в антипартизанской борьбе оказывали, 
  как явствует из партизанских донесений, жители следующих деревень: в зоне 1-го 
  района — Ворон, Ай-Серез, Шелен, Отузы, Кутлак, Суук-Су, Капсихор, Таракташ; 
  в секторе деятельности отрядов 2-го района — Арпат, Коперликой, Соллар, Юхары-Баши, 
  Султан-Сарай, Молбай, Казанлы, Камышлык, Кишлав, Куртлук, Баксан, Улу-Узень, 
  Куру-Узень, Ускут, Ени-Сала; в 3-м и 4-м районах и зоне расположения центрального 
  штаба — Коуш, Бешуй, Корбек, Стиля, Саблы, Биюк-Янкой, Терскунда, Узенбаш, Мангуш, 
  Албат, Фоти-Сала, Дегерменкой и другие южнобережные сёла; в 5-м районе — … Кок-Козы, 
  Алсу, Уппа, Ай-Тодор, сёла Байдарской долины. И это не исчерпывающий список 
  деревень, где находились отряды самообороны. Конечно, не все они были чисто 
  крымскотатарскими, например в Саблах и Мангуше крымских татар было сравнительно 
  немного, но в большинстве из них татарское население преобладало.<br>
  <br>
  Особенно досаждал партизанам отряд, который одним из первых был образован в 
  дер. Коуш Куйбышевского района. Этот населённый пункт играл стратегическую роль, 
  глубоко вклиниваясь в горно-лесную зону как раз на стыке 3-го и 4-го партизанских 
  районов. Он оказался весьма удобным опорным пунктом для развертывания антипартизанских 
  операций в лесах Госзаповедника, где до февраля 1942 г. располагался штаб Мокроусова. 
  После первых же столкновений с партизанами немцы укрепили подходы к деревне 
  земляными дотами и кое-где минировали подступы к ней. Первоначально коушский 
  отряд насчитывал 80 человек, затем вырос до сотни, став основой для развёртывания 
  в этом населенном пункте «роты татарской самообороны». Однако ещё до того, как 
  коушский «гарнизон» получил регулярный статус, его ополченцы неоднократно обстреливали 
  партизанские отряды вблизи своего населенного пункта. Так, 1 января 1942 г. 
  они захватили и расстреляли партизанских разведчиков, 3 января организовали 
  засаду, в которую попал 2-й симферопольский отряд Х. Чусси, потерявший в бою 
  7 человек убитыми, 8 ранеными и 8 пропавшими без вести[61]. Коуш также использовался 
  как место для обучения нового пополнения добровольцев.<br>
  <br>
  В январе 1942 г. командование 11-й армии провело реорганизацию подразделений 
  «самообороны». Она была вызвана высадкой советских десантов под Керчью и Феодосией 
  и связана с намерением использовать крымских татар в составе регулярных армейских 
  частей. Порядок придания «вспомогательным подразделениям» регулярного характера 
  всесторонне исследован О.В. Романько[62]. Согласно немецким документам, в январе 
  1942 г. в 203-х населённых пунктах и 5-ти лагерях военнопленных было набрано 
  9255 крым¬ских татар, из которых 8684 человека, признанных годными, были направлены 
  в части вермахта, штурмующие Севастополь, и на Керченский фронт. Одновременно 
  Айнзацгруппа «Д» создала 14 «татарских рот самообороны» в составе 1632 человек, 
  на которые были возложены задачи активного поиска партизанских отрядов, осуществление 
  карательных экспедиций и даже боевые действия против советского десанта в районе 
  Судака. Эти подразделения создавались и действовали под непосредственным руководством 
