  <br>
  С самого начала боевых действий вопрос отношений с местным населением был жестко 
  определён п. 13 первого же приказа Мокроусова: «С населением районов установить 
  самые тесные и дружеские отношения, не допускать грубостей и всякого рода реквизиций, 
  за взятые у населения продукты, скот и имущество — выплачивать его стоимость. 
  Всячески помогать населению и оказывать ему посильную помощь»[87]. Другого было 
  бы трудно ожидать, ибо партизаны могли действовать только за счет поддержки 
  местного населения. Документы позволяют реконструировать последовательность 
  разворачивания конфликта: разгром парти¬занских баз оккупантами при активном 
  участии местного населения, порожденный им голод в отрядах, вызванное голодом 
  дезертирство, сопровождаемое предательством, толкали партизан на ответные реквизиции 
  и другие недружественные местному татарскому населению действия, последние же 
  приводили к росту добровольческих карательных подразделений. Именно такую картину 
  рисует командир 3-го района Г.Л. Северский в своей Докладной записке в обком 
  ВКП(б): «В середине января кончились запасы продовольствия на партизанских базах. 
  Все попытки по установлению связи с населением и организации приобретения продовольствия 
  в населенных пунктах результатов не давали. Командование было вынуждено стать 
  на путь насильственного изъятия скота и продовольствия у враждебно относящегося 
  к нам населения. Немецкое командование не замедлило использовать и этот факт 
  в своей агитации, дабы усилить активность отрядов местной самообороны»[88]. 
  Нападения на татарские сёла объяснялись партизанами большей частью как «акты 
  возмездия» и преследовали цель «отобрать отобранное», т.е. вернуть захваченное 
  с баз или компенсировать это захватом скотины и продуктов[89], и здесь трудно 
  было рассчитывать на какую-то разборчивость. Безусловно, партизаны не особенно 
  выясняли, кто прав, а кто виноват. Этот факт в 1942 г. признал в своём рапорте 
  командир 4-го района И. Вергасов, который сообщал, что изъятие скота производилось 
  кое-где без разбора, а при конфискации продовольствия «забранного с наших баз… 
  иногда допускались ошибки»[90]. Однако следует учесть, что так называемые «продоперации» 
  были жестами отчаяния, они осуществлялись часто просто пухнувшими от голода 
  людьми и большей частью не приводили к положительным результатам. Именно в «продоперациях», 
  т.е. нападениях на сёла, обозы с продуктами, а то и просто в походах на заброшенные 
  колхозные поля для того, чтобы накопать мёрзлой картошки, гибло наибольшее количество 
  людей, слабо боеспособных, измученных голодом. Красноречивую картину продопераций 
  нарисовал в своем полном отчаяния и гнева письме Крымскому обкому назначенный 
  взамен Мартынова комиссар партизан Крыма Н. Луговой, говоря о второй вспышке 
  голода в июле-августе 1942 г.: «Немцы предприняли контрмеры: у всех крестьян 
  прилесных деревень забрали скот и угнали подальше от леса, а деревни укрепили 
  специальными гарнизонами — полицейскими татарскими отрядами, которые, устраивая 
  засады против партизан, вынудили нас даже яблоки в общинных садах брать с боем… 
  В боях за продовольствие в течение этих двух месяцев мы потеряли своих людей 
  больше, чем в июльских боях с карательной экспедицией, имели гибель партизан 
  и от голода (а сколько потеряли мы в главном — в боевой работе, ибо потеряли 
  боеспособность, да воевали за яблоки и картофель!)[91]». Об одной из таких продопераций 
  рассказал бывший боец 3-го симферопольского отряда А.А. Сермуль: «Как-то еще 
  в голодное время партизаны муковнинского отряда выходили на продоперацию на 
  заброшенное колхозное поле, копали оставшуюся там картошку. Однажды нарвались 
  на засаду — всех добровольцы перебили, а одного тяжело раненого привезли, точнее 
  волоком за лошадью притащили, и бросили на площади у сельсовета — для опознания. 
  Он лежит, стонет, пить просит. Так вот (местный) полицай подошел и на глазах 
  у всех помочился ему в рот…» (этого сабловского полицая, судя по всему не крымского 
  татарина, партизаны судили и повесили в 1943 г.)[92]. Большая часть «продопераций» 
  против деревень заканчивалась для партизан только дополнительными жертвами, 
  так что вряд ли они наносили какой-либо серьёзный ущерб крымскотатар¬скому населению, 
  хотя и способствовали росту неприязни к партизанам.<br>
  <br>
  Говоря о «насилии партизан» по отношению к местному населению, следует учитывать 
  и то, что для разжигания вражды между населением и партизанами немцы прибегали 
  к масштабным провокациям. Как утверждал А.А. Сермуль тайной полевой полицией 
  (ГФП) были созданы лжепартизанские отряды, задачей которых было производить 
  нападения на деревни под видом партизан[93]. По свидетельству И.Г. Генова, летом 
  1942 г. такие нападения были совершены на деревни Пролом, Алач, Карачёль, Аблеш 
  и Кокташ, население которых хорошо относилось к партизанам. Эти действия были 
  разоблачены, т.к. одним из «партизан» в виде доказательства был оставлен обрез, 
  которым партизаны не могли быть в то время вооружены[94]. <br>
  <br>
  <img src="a-partizkrym14.jpg" width="198" height="143"><br>
  <br>
  Много шума еще в 1941—42 гг. наделало якобы имевшее место распоряжение Мокроусова 
  о расстреле всех появляющихся в лесу татар. Р. Музафаров приводит следующие 
  слова бывшего командира севастопольского отряда М. Зинченко: «Это было приблизительно 
  в последних числах 1941 или в начале 1942 г., и это было распоряжение Мокроусова. 
  Всех шатающихся в лесу татар — расстреливать, таков был приказ. Я лично читал 
  этот приказ и выполнял его. Может быть он (местный житель. — А.М. ) просто шел 
  в лес, а мы их ловили и расстреливали»[95]. Этот «приказ» Мокроусова фигурирует 
  едва ли не во всех посвященных его критике сюжетах в литературе, однако история 
  с ним выглядит весьма сомнительной. Ничего подобного в документах штаба Мокроусова 
  не обнаружено. Работник ОО НКВД Крыма Харченко сообщает в своей докладной записке, 
  что такой приказ был, но относился он не к татарам, а… к военнослужащим Красной 
  Армии. Якобы в декабре 1941 г. связной 2-го района Гаркавенко передал «словесное 
  приказание Мокроусова командованиям отрядов: «Всех шатающихся в лесу военных 
  группами и в одиночку — пристреливать»[96]. Излишне говорить, что если бы это 
  нашло подтверждение, Мокроусову по возвращении на Большую землю не сносить головы, 
  учитывая отношение к нему маршала Будённого и наличие многочисленных и влиятельных 
  врагов, однако все дело ограничилось «почетной ссылкой», значит, в действительности 
  такого приказа не существовало. Не так ли обстояло дело и с приказом о «шатающихся 
  по лесу татарах»? На заседании бюро обкома в октябре 1942 г., расследовавшего 
  деятельность Мокроусова, об этом «приказе» не упоминалось, хотя другие «ошибки» 
  командующего стали предметом тщательного рассмотрения. Нет ничего о существовании 
  этого «приказа» и в тексте постановления по «татарскому вопросу» (в нем просто 
  говорится, что «всех попавших в лес граждан — расстреливали»). Конечно, нельзя 
  исключить, что Мокроусов и, возможно, командиры районов и отдельных отрядов 
  в конкретных случаях отдавали подобного рода распоряжения (очевидно, читателю 
  понятно, что боевой приказ и устное распоряжение — это разные вещи) относительно 
  появлявшихся в лесу местных жителей, в том числе татар, среди которых очень 
  часто попадались и вражеские разведчики и проводники.[97] Однако очевидно, что 
  целенаправленной установки на уничтожение «всех подозрительных» все же не было. 
  Известно, что, несмотря на блокаду леса оккупантами, в 1941—42 гг. достаточно 
  долгое время функционировали лесные дороги через партизанский район, по которым 
  местное, прежде всего крымскотатарское население осуществляло передвижение и 
  обмен: из района Ялта-Симеиз через Ай-Петри на Коккозы; из района Кучук-Узень 
  через Караби-яйлу на Баксан и из района Ворон-Арпат-Капсихор на Орталан[98]. 
  В лесу довольно часто появлялись «посторонние», и даже тот факт, что группа 
  того же М. Зинченко отобрала продукты у каких-то татар в лесу, свидетельствует 
  о том, что свободное перемещение по лесным дорогам всё же существовало, а значит, 
  чего-то даже отдаленно напоминающего организованный террор партизан против местного 
  татарского населения в действительности не было. То же следует сказать и о просьбах 
  партизанского руководства «стереть с лица земли» татарские сёла, служившие базами 
  для отрядов самообороны.<br>
  <br>
  В 1957 г. М. Селимов и Р. Мустафаев свидетельствовали: Мокроусов и Мартынов, 
  «чтобы скрыть свою личную беспомощность в борьбе с оккупантами и свалить грехи 
  на местных жителей», якобы неоднократно обращались к командованию с просьбой 
  выслать боевые самолёты для бомбардировки «мирных татарских сёл… Одновременно 
  они давали приказы по партизанским отрядам сжечь и стереть с лица земли татарские 
  деревни и уничтожать их ни в чём не повинных жителей»[99]. Здесь мы легко можем 
  оказаться в плену неправильного понимания экстремальной фронтовой лексики. Действительно, 
  начальник особого отдела Попов (а не Мокроусов) в сердцах однажды (а возможно 
  и не один раз) сообщил на Большую землю, что «для того, чтобы облегчить борьбу 
  партизан 3-го и 4-го районов, необходимо стереть с лица земли следующие населенные 
  пункты татар: 1. Село Коуш — штаб формирования татар; 2. Село Бешуй — где всегда 
  производится концентрация немецких войск для нападения на партизанские отряды; 
  3. Село Корбек — штаб формирования татарских отрядов». Это мотивировалось тем, 
  что: «своими силами партизаны уничтожить эти населенные пункты не могут, так 
  как… они сильно укреплены»[100], однако, как мы видим, здесь речь идет совсем 
  не об уничтожении мирного населения деревень, а о ликвидации баз карателей. 
  В партизанских радиограммах всегда имеются в виду именно войска: «Настоятельно 
  просим, — радировал в феврале 1942 г. Генов, — бомбить скопления войск противника 
  в Шелене, Арпате, Ускуте, а также на восточном склоне занимаемой нами высоты»[101]. 
  «22.2. 42, — сообщает Генову Мокроусов, — в Улу-Узень прибыло 300 румын и 100 
  татар-добровольцев, которых румыны обмундировывают. В связи с предполагаемой 
  выброской грузов есть опасение выхода румын на яйлу и р-н Тырке. Желательно 
  было бы в день выброски грузов разбомбить Улу-Узень…»[102]. При этом следует 
  учитывать, что реально партизанам не приходилось рассчитывать на помощь боевой 
  авиации, которой на юге было крайне мало[103]. Насколько нам известно, в 1941—1942 
  гг. авиация задействовалась очень редко и только против войск (включавших, между 
  прочим, и крымскотатарских добровольцев) и их опорных баз. Возможно, и в это 
  время и впоследствии, в 1943 и 1944 гг., под бомбежки и попадало мирное население 
  некоторых татарских населённых пунктов, как это случалось и в других местах 
  Крыма, но само население не могло быть собственно объектом бомбометания, для 
  которого у военной авиации были несравнимо более приоритетные цели. Что касается 
  немецкой авиации, то именно она неоднократно бомбила населённые пункты, занятые 
  зимой — весной 1942 г. партизанами в зоне 2-го района, и её задачей было именно 
  уничтожение населения и разрушение жилищ, где могли укрыться партизаны.<br>
  <br>
  Нет никаких оснований утверждать, что объектом враждебности со стороны партизан 
  когда-либо становилось мирное население как таковое (даже нелояльно настроенное). 
  Следует учесть, что в январе 1942 г. партизаны, в частности 2-го района, неодократно 
  захватывали деревни, в том числе и те, где были предатели, старосты, полицейские 
  и даже стояли подразделения самообороны, однако ни в немецких, ни в партизанских 
  документах мы не находим сведений об осуществлении каких-либо более или менее 
  масштабных репрессий против населения, в том числе против семей старост, полицейских 
  и т. д. (хотя, конечно, изобличенные в сотрудничестве жители расстреливались, 
  причем без «вхождения в обстоятельства», при этом за период боевых действий 
  всего было уничтожено до 400 человек «активных предателей»), а вот факты таких 
  репрессий со стороны оккупантов и добровольцев после оттеснения партизан — весьма 
  многочисленны. <br>
  <br>
