  ночь она не утихала. На рассвете к врагам подошло еще подкрепление. Парашютисты 
  отбивались отчаянно. Когда предатели поняли, что десантников живыми не взять, 
  облили дом керосином и подожгли. Из него так никто и не вышел — все сгорели, 
  но в плен не сдались...»</strong></em> [10].<br>
  <br>
  Стоп. В доселе цитированной литературе речь идет о моряках, тут — парашютисты, 
  не имевшие никакого отношения к собственно судакскому десанту. Считалось, что 
  погибшие в Вороне были убиты немцами, здесь же фигурируют «местные полицейские»... 
  Снова путаница? Но просто отмахнуться от текста невозможно. Если в первых случаях 
  речь идет о литературном творчестве журналистов, то тут непосредственные воспоминания 
  участника событий, к тому же разведчика. Такие люди редко что-то путают. Понятно 
  только одно — доступной литературы явно недостаточно. Придется обращаться к 
  архивным данным.<br>
  <br>
  Относящиеся к периоду Великой Отечественной войны документы, в том числе и материалы 
  партизанского движения, хранятся в бывшем Крымском областном партархиве (ныне 
  отделе Государственного архива в Автономной Республике Крым). Долгое время хранителем 
  партархива являлся И.П. Кондранов. Незадолго до своей смерти он опубликовал 
  подневную хронику событий в Крыму в годы войны: «Крым 1941—1945» [11], целиком 
  основанную на документах архива. Не мог же он пройти мимо такого во всех смыслах 
  неординарного факта? И действительно, под датами 19—20 января в этой хронике 
  приведена следующая информация: <em>«Боевая группа советских парашютистов, выброшенная 
  9 января в районе Старого Крыма, зашла в дер. Ворон с намерением пополниться 
  продуктами. Хозяин дома на окраине деревни, к которому обратились десантники, 
  послав свою дочь якобы за продуктами, созвал вооруженных «самооборонцев» не 
  только своей деревни, но также из Ай-Сереза, Шелена, Капсихора, чтобы расправиться 
  с советскими воинами. Вся группа, в которую входили младший сержант К.П. Юргенсон, 
  рядовые краснармейцы А.В. Зайцев, Н.И. Демкин, М.Г. Кохаберия, Л.И. Нетронькин, 
  Н.Х. Трегулов, А.В. Богомолов, В.С. Быков, А.К. Борисов, Б.Д. Адигиезалов, К.А. 
  Колясников, Г.Г. Казарьян, погибла»</em> [12].<br>
  <br>
  К фрагменту «Хроника» Кондранов привел и обширный список архивных ссылок — все 
  они указывают на различные дела архива партизанского движения в Крыму в период 
  Великой Отечественной войны. Тщательно переплетенные картонные папки бережно 
  хранят на первый взгляд невзрачные, часто плохо читаемые записки, донесения, 
  приказы, списки, отчеты о боевых действиях, дневники... Ни одна самым роскошным 
  образом изданная грамота не производит более сильного впечатления, чем эти серые, 
  исписанные неровным почерком и часто с грамматическими ошибками листки, со страниц 
  которых веет духом трагедии и самопожертвования. <br>
