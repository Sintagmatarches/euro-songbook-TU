	<strong>Андрей 
  Мальгин, кандидат филологических наук, генеральный директор Крымского республиканского 
  учреждения Центральный музей Тавриды</strong><br>
  <br>
<span class="header"><strong>ЗАГАДКА ОБЕЛИСКА В СЕЛЕ ВОРОН: ОПЫТ ИСТОРИЧЕСКОГО РАССЛЕДОВАНИЯ</strong><br></span>
  <br>
  <a href="http://old.commonuments.crimea-portal.gov.ua/rus/index.php?v=1&tek=101&par=74&l=&art=553" target="_blank"><em>Журнал &quot;Историческое наследие Крыма&quot;, №17, 2006</em></a><em>.</em><br>
  <br>
  <br>
  Чем больше отделяет нас время от Великой Отечественной войны, тем более очевиден, 
  во всяком случае для автора этих строк, факт какого-то особенного влияния, которое 
  оказывают те далекие события на нашу сегодняшнюю жизнь. Речь идет не просто 
  о мистическом притяжении эпохи Грандиозных Событий, что «видятся на расстоянии». 
  Война знаменует собой тектонический сдвиг в истории нашего отечества и в его 
  судьбе. Особенно это чувствуется здесь, в Крыму, основные и наиболее острые 
  проблемы и беды которого так или иначе уходят своими корнями в минувшую войну. 
  По мере осознания этой своеобразной и неотвратимой зависимости нашего «сегодня» 
  от военного «вчера» растет и еще одно ощущение: мы очень мало знаем о той войне, 
  которую пережили наши деды и, может быть, поэтому до сих пор не в состоянии 
  преодолеть порожденные ею травмы. Та война нависает над нами как большая загадка, 
  если угодно, загадка происхождения нашего сегодняшнего состояния, тайна нашего 
  бытия. Она, в свою очередь, распадается на мириады затерянных в войне, как в 
  снежной пустыне, загадок отдельных человеческих судеб — тех, кто «благополучно» 
  пережил войну, и в особенности тех, кто не вернулся с нее. История, о которой 
  дальше пойдет речь, — одна из таких неразрешенных загадок, прикасаясь к которым 
  мы надеемся когда-нибудь разгадать большую и страшную тайну той великой войны.<br>
  <br>
  ***<br>
  <br>
  В нескольких километрах от Судака, в складках горных долин, расположилось маленькое 
  село со странным названием — Ворон. Сельцо это далеко от суеты курортного побережья, 
  и живет в нем сегодня едва ли более полутора сотен человек. В центре его, рядом 
  со старой мечетью и пересохшим фонтаном, построенным, как явствует из расколотой, 
  но не выпавшей из своего гнезда мраморной плиты, в 1925 году, по большевистской 
  программе «лицом к деревне», среди высоких деревьев стоит обелиск, а за ним 
  надгробная плита на братской могиле. Надпись на плите гласит:<br>
  <br>
  <em>Вечная слава героям,<br>
  павшим за свободу и независимость Родины.<br>
  6.III.1942<br>
  <br>
  Матрос Авдиенко<br>
  <br>
  Матрос Котяников Н.Г.<br>
  <br>
  Матрос Корукин С.Г.<br>
  <br>
  Матрос Нестеренко<br>
  <br>
  Матрос Пековцев<br>
  <br>
  Матрос Ремень Р.С.<br>
  <br>
  Старшина Резников<br>
  <br>
  Матрос Тёмкин А.Н.<br>
  <br>
  Матрос Котельников Н.П.<br>
  </em><br>
  Любой экскурсовод, водящий группы туристов по окрестностям Судака, обязательно 
  расскажет вам историю о гибели в селе Ворон группы советских десантников, высаженных 
  в тыл противника во время знаменитой Керченско-феодосийской десантной операции. 
  В самой обобщенной передаче, к примеру в той, что изложена в недавнем путеводителе 
  по Судаку А. Тимиргазина [1], эта история выглядит следующим образом Высадившись 
  в районе Судака в январе 1942 года, группа десантников вела бои с немцами. Намереваясь 
  пробиться к партизанам, оставшиеся без продуктов и изрядно вымотанные, бойцы 
  зашли в село Ворон, постучались в крайний дом, надеясь отдохнуть. Хозяин впустил 
  десантников, но тайно предупредил немцев, которые окружили дом и предложили 
  бойцам сдаться. Те отказались и стали отстреливаться. Тогда оккупанты обложили 
  дом соломой и хворостом и подожгли его, «семеро десантников погибли под рухнувшей 
  крышей» [2]. <br>
  <br>
  <img src="a-zagobelisk1.jpg" width="198" height="208"><br>
  <br>
  Некоторые жители Ворона до сих пор показывают место, где стоял дом, ставший 
  последним рубежом для этих бойцов.<br>
  <br>
  Сравнительно недавно и мне пришлось побывать в Вороне и своими глазами увидеть 
  могилу, где похоронены десантники, сухой фонтан и мечеть. Сидя на лавочке среди 
  старых деревьев в месте, которое само по себе должно исключать всякую суетность, 
  я вдруг поймал себя на том, что постепенно от «возвышенных размышлений» перешел 
  к сугубо «профессиональным» сомнениям, возможно не вполне уместным здесь. Мне 
  подумалось о двух разночтениях: в экскурсионном рассказе и в путеводителе, где 
  приводится текст, начертанный на могильной плите, говорится о семи десантниках, 
  а здесь, на плите, — девять фамилий. Пустяк в общем-то, ошибка вполне возможная 
  и не такая уж редкая в подобной литературе. А вот другое несоответствие существеннее: 
  события судакской десантной операции имели место в январе 1942 года, дата же 
  на плите — 6 марта 1942 года. Между высадкой и гибелью слишком большой разрыв, 
  не могли же десантники блуждать по лесу в поисках партизан полтора месяца? В 
  это время в прилегающих лесах дислоцировались как минимум четыре партизанских 
  отряда общей численностью около 500 человек, которые вели активную боевую деятельность 
  и разведку. Все пространство лесов с севера на юг проходимо за несколько часов. 
  От побережья до ближайших партизанских лесов не более 7-8 километров, преодолеваемых 
  максимум за два часа. Не иначе что-то напутали при установке обелиска.<br>
  <br>
  <img src="a-zagobelisk2.jpg" width="198" height="274"><br>
  <br>
  Что ж, хороший повод по возвращении в Симферополь обратиться к доступной литературе 
  — может быть, она что-либо прояснит. Поскольку речь идет о памятнике, следует 
  начать с главного «монументного» реестра — Свода памятников Автономной Республики 
  Крым. Увы, обращение нему ничего существенного не дает: под № 967 в селе Ворон 
  (парк) Междуреченского сельсовета в реестре действительно значится Братская 
  могила советских воинов (перезахоронение в 1966 г.), однако кроме того, что 
  она поставлена на учет решением крымского облисполкома от 05.09.1969 № 595 и 
  что в 1980 году была установлена охранная зона этого памятника, другой информации 
  не имеется [3]. Впрочем, это только начало — всякий памятник имеет паспорт, 
  к которому прилагается историческая справка, где и должна быть зафиксирована 
  необходимая нам информация. Паспорта хранятся в рескомитете АРК по охране культурного 
  наследия. По совету одного из старейших «памятниковедов» Крыма В.Н. Гурковича 
  обращаюсь туда и вскоре получаю ксерокопию со следующими сведениями: <em>«Братская 
  могила советских воинов. Расположена в центре парка с. Ворон. В январе 1942 
  г. при выполнении директивы Военного Совета Кавказского фронта об общем наступлении 
  в Крыму для отвлечения сил противника от Феодосийского направления в районе 
  Судака были высажены десанты. После тяжелых боев десантники ушли к партизанам. 
  При переходе одна из групп на окраине села Ворон попала в засаду и в одном из 
  домов вела неравный бой в окружении. В подожженном гитлеровцами доме десантники 
  отстреливались до конца и 6 марта 1942 г. героически погибли с пением Интернационала. 
  В 1966 г. останки 9 советских моряков перезахоронены в центре села. На братской 
  могиле установлен памятник-обелиск... и надгробие»</em> [4]. Далее приводятся 
  сведения о размерах и материале памятника, надписях на них.<br>
  <br>
  Прямо скажем — негусто. Хронологическое недоразумение так и не разрешилось, 
  хотя появились подробности о «пении «Интернационала». Но к документу приложена 
  библиография, и это уже кое-что. Правда, литературы в ней немного, точнее, всего 
  два источника: книга П.А. Моргунова «Героический Севастополь» (М., 1979. — С. 
  250—254) и работа Г.С. Сергеевой «Алушта — Судак», изданная в Симферополе в 
  1970 году (С. 64—69). Что ж, обращаемся к ним. Фундаментальное сочинение Моргунова 
  дает лишь общий, вполне добротный событийный контекст и оперативный очерк о 
  судакской операции без углубления в детали. А вот скромная книжка-путеводитель 
  Г. Сергеевой сообщает и некоторые подробности, правда, в слишком беллетризированном 
  виде. После рассказа о том, что десантники в январе 1942 года оказались в фашистском 
  кольце и часть сумела прорваться к партизанам, Г. Сергеева повествует о судьбе 
  одной из групп, оказавшейся в селе Ворон:<em> «Неизвестно, — пишет она, — как 
  шли сюда в январе 1942 года семеро десантников. Их путь прослежен пока только 
  в самом Вороне. Пастух, которого встретили они в уединенном домике, невдалеке 
  от села, сказал: «Идите в Ворон смело, фашистов там нет». Моряки пошли. Они 
  смертельно устали, очень долго не ели... Зашли в дом на краю села. Они не успели 
  поесть, когда услышали шум мотора. Бросились к окнам и увидели, как с грузовика 
  спрыгивают гитлеровцы. Поняли — предательство... Моряки открыли огонь. Девять 
  гитлеровцев один за другим свалились возле дома. Оставшиеся фашисты подожгли 
  дом. Металось, гудело пламя. И вдруг послышался “Интернационал”...»</em> [5] 
  Надо отдать должное автору путеводителя — в отличие от авторов более поздних 
  пересказов этой истории, она подчеркнула легендарный характер этих сведений 
  и, что самое важное, отослала нас к источникам своей информации. Последних — 
  два. Это очерк крымской журналистки Л. Литвиновой, основанный, в свою очередь, 
  на рассказах одного из жителей села Ворон П.В. Петрунина.<br>
  <br>
  Очерк Л. Литвиновой «Пепел и алмаз» был опубликован в авторском сборнике журналистки 
  «Четверо в шлюпке», выпущенном издательством «Крым» в 1967 году. Именно он сделал 
  воронскую легенду «достоянием широкой общественности». В очерке рассказывается, 
  как однажды в 60-х годах автор оказывается в селе Ворон, название которого заставило 
  вспомнить о «страшной истории», услышанной когда-то от кого-то из бывших партизан, 
  и задается целью узнать о подробностях воронской трагедии. Беседа с жителями 
  села приводит ее к знакомству с П.В. Петруниным, местным энтузиастом, который 
  долго разыскивал сведения о погибших десантниках и занимался перезахоронением 
  их останков. Со слов Петрунина, Л. Литвинова передает «воронскую быль»: <em>«...В 
  ночь с 19 на 20 января 1942 года {...} в Новосветской бухте под Судаком высадился 
  еще один десант, так называемый демонстрационный. В морозную тьму побережья 
  вступила небольшая группа краснофлотцев...» </em>После гибели политрука семеро 
  матросов <em>«...долго искали партизан, чтобы передать собранные сведения на 
  Большую землю»</em>, продовольствия не было и партизан тоже, десантники <em>«то 
  от преследования уходили, то в бой вступали... в начале марта вышли... в окрестности 
  села Ворон. Зашли в сторожку к чабану» </em>[6], который посоветовал идти в 
  село, так как немцев там вроде бы не было. Поскольку <em>«заявляться в село 
  в драных черных шенелях, в бескозырках со звездами...» </em>было рискованно, 
  десантники отправили на разведку одного из бойцов группы. Жители деревни опасались 
  оказывать помощь, пока один из них не сказал моряку: <em>«Веди всех в мой дом, 
  накормлю».</em> Далее в основном повторяется рассказ, приведенный Г. Сергеевой 
  и повторенный потом всеми прочими путеводителями, правда, в еще более беллетризированной 
  форме (что, конечно, следует отнести на счет жанра и эпохи). В частности, бойцы 
  кроме «Интернационала» спели якобы песню о Москве, рвали на себе тельняшки [7] 
  и т. д., в общем, делали все то, что и положено делать окруженным врагом советским 
  матросам в любом патриотическом фильме «про войну». После гибели бойцов в огне, 
  сообщается в очерке, кто-то из жителей тайно похоронил их останки и поставил 
  над могилой крест. Уже после войны его заменили на деревянный обелиск со звездой, 
  а потом перенесли могилу, так как ее начала подмывать разливавшаяся речка...<br>
  <br>
  В каком-то смысле воронская ситуация 1942 года прояснилась, но в каком-то — 
  запуталась еще сильнее. Появляется новая дата — 19—20 января и более точное 
  место высадки десантников — Новый Свет, но в эти дни в Новом Свете никакой высадки 
  не было. И снова, теперь уже Петрунин, который сам организовывал перезахоронение, 
  говорит о семи десантниках. На обелиске же девять фамилий...<br>
  <br>
  Важный вывод, который следует из очерка Литвиновой, — сам Петрунин не был близко 
  знаком с очевидцами воронской трагедии: он приехал в Ворон в апреле 1944 года, 
  «сразу после освобождения Крыма», а в мае все непосредственные свидетели отправились 
  на спецпоселение в Среднюю Азию, поскольку все они были крымскими татарами. 
  Как и все остальные, Петрунин лишь слышал легенду о погибших десантниках и видел 
  их первоначальную могилу с крестом. Откуда же ему стали известны фамилии погибших 
  и даты событий? Здесь у авторов очерков разночтения: Г. Сергеева утверждает, 
  что фамилии были уже написаны кем-то на дощечке, прибитой к кресту, когда его 
  впервые увидел Петрунин. Литвинова же, на которую ссылается Сергеева, напротив, 
  ничего подобного не сообщает. Из ее очерка следует, что установление имен погибших 
  — результат самостоятельного расследования, проведенного Петруниным. Впрочем, 
  и Г. Сергеева косвенно подтверждает это же:<em> «сам солдат, сражавшийся с врагом 
  в крымских лесах и под Севастополем (Петрунин. — А.М.)... не мог смириться с 
  тем, что останутся эти семеро почти безвестными — ведь кроме воронцев, никто 
  не знал о них. И Петрунин стал слать письма и запросы в архивы. Приходили ответы, 
  а он снова писал. И опять приходили ответы, теперь уже от родственников погибшх. 
  Они благодарили солдата, а потом приезжали в Ворон и долго стояли с Петруниным 
  у могилы»</em> [8]. Даже не представляя, добавим мы, что перед воронским энтузиастом 
  стояла почти неразрешимая задача, ведь пытаться установить личности семи или 
  девяти погибших в окрестностях Судака в начале 1942 года — все равно что искать 
  иголку в стоге сена…<br>
  <br>
  В районе Судака в январе 1942 года имели место четыре высадки советских войск. 
  1 января, одновременно с десантированием частей 44-й армии в Феодосии, в Коктебель 
  и район Нового Света с подлодок и катеров Черноморского флота были высажены 
  разведгруппы ЧФ c задачей — выявить противодесантную немецкую оборону. Затем, 
  в ночь на 6 января, в район Нового Света снова был высажен передовой отряд тактического 
  десанта в количестве 218 человек из состава 226-го горнострелкового полка 63-й 
  дивизии 44-й армии. В ночь с 15 на 16 января высажена основная часть 226-го 
  полка. И в ночь на 25 января в Судак был высажен 554-й сп, который присоединился 
  к ранее высаженной группе. Всего в Судакской бухте высадились около 3 тысяч 
  человек, большинство из них погибло там же, в окрестностях. Теоретически в Вороне 
  могли оказаться участники любой из высадок. К сожалению, мы, наверное, так и 
  не узнаем, почему Петрунин искал среди погибших в Вороне именно тех, кто высаживался 
  в первом эшелоне Керченско-феодосийской операции, и почему он полагал при этом, 
  что их высадка имела место 19—20 января, а не 1-го, как это было в действительности, 
  не говоря уже о том, почему он относил дату их гибели к 6 марта.<br>
  <br>
  Воронский энтузиаст проделал большую работу как умел. Он установил фамилии моряков, 
  высаживавшихся в Новосветской бухте январской ночью 1942 года. Ему удалось даже 
  установить и некоторых оставшихся в живых участников этих событий. Л. Литвинова 
  приводит имена В. Овсиевского и И. Лещенко, которые входили «в разные группы» 
  и смогли уцелеть в мясорубке начала 1942 года и проживали после войны первый 
  — в Кировоградской, второй — в Харьковской области. Попавшая в литературу и 
  воплощенная в камне воронская легенда зажила собственной жизнью и со временем, 
  как водится, стала обрастать подробностями.<br>
  <br>
  В 1983 году появилась книга, посвященная героям судакского и коктебельского 
  десантов, — небольшой сборник очерков Г.А. Корабельского «Десантники», выпущенный 
  в симферопольском издательстве «Таврия». Очерки эти также не являлись исследованием 
  по военной истории, а были написаны скорее в воспитательных целях и представляли 
  собой авторскую реконструкцию событий, заинтересовавших автора. Он собрал несколько 
  свидетельств участников январских боев в Судаке и Коктебеле и изложил их в духе 
  лихого сценария военного боевика советского времени. Не обошел он вниманием 
  и воронский эпизод. Да что там — не обошел! Нашел участника воронских событий 
  — моряка из погибшей группы десантников, который чудом уцелел в том аду. Вспомним: 
  Л. Литвинова утверждает, что Петрунин установил, будто два десантника — В. Овсиевский 
  и И. Лещенко, которые «входили в разные группы», — живы. Так вот у Корабельского 
  один из них, а именно Осиевский (так, без «в») вдруг оказывается участником 
  воронской драмы. Согласно событийной реконструкции Корабельского, Осиевский 
  был послан в караул, но был ранен и захвачен немцами, которые не убили его как 
  остальных десантников, а бросили в кузов машины и увезли. Впоследствии он попал 
  в лагерь для военнопленных, бежал и снова воевал, дошел до Югославии, потом 
  работал в колхозе в Кировоградской области [9]. Честно говоря, выглядит этот 
  эпизод не очень убедительно. Во-первых, Петрунин, у которого Корабельский заимствовал 
  всю интерпретацию событий в Вороне (при этом ни его самого, ни Л. Литвинову 
  ни словом не упомянул), ничего не говорил о причастности Осиевского к воронскому 
  эпизоду. Этот десантник действительно входил в «другую группу», а именно в ту, 
  что высаживалась в Коктебеле 1 января. И Корабельскому пришлось придумывать 
  целую историю для того, чтобы «привести» его, а заодно и еще одного моряка — 
  Н. Котельникова — из Коктебеля в окрестности Судака на встречу с «судакской 
  группой», а с ней — в Ворон. Во-вторых, в целом текст Корабельского являет собой 
  такой крутой литературный замес, в котором просто растворяются все элементы 
  реальной действительности. Что в ней вымысел, а что реальность, наверное, не 
  смог бы сказать и сам автор. Несомненно, что-то в истории с Осиевским имело 
  место на самом деле, но вот в то, что все это, т. е. пленение, ранение и т. 
  д., произошло с ним именно так и именно в том месте, верится с трудом. Наконец, 
  снова всплывает хронологическая путаница. Корабельский отнес воронский эпизод 
  не к марту 1942 года, как почему-то полагал Петрунин, а к первым числам января 
  (видимо, для того чтобы с «введением» в повествование «очевидца» заполнить тот 
  временной вакуум, который отделял его высадку в Коктебеле от воронского события 
  по датировке Петрунина).<br>
  <br>
  Но сомнения сомнениями, а альтернативы версии воронского энтузиаста П.В. Петрунина 
  будто в селе Ворон были злодейски умерщвлены моряки — участники одной из судакских 
  высадок января 1942 года, получалось, действительно нет. Она началась со смутной 
  легенды и обрела свою законченную форму в целом ряде литературных повествований 
  и при этом дополнилась множеством подробностей. Что же до путаницы в датах, 
  которую мы так и не объяснили, то, увы, это происходит сплошь и рядом даже с 
  известными историками, что же говорить о краеведах и журналистах? На этом можно 
  было бы поставить точку, если бы не еще одно, достаточно давнее литературное 
  свидетельство, которое буквально все перевернуло в воронской истории.<br>
  <br>
  В 1986 году в Москве вышла книга С.Н. Выскубова о событиях в Крыму во время 
  Великой Отечественной войны. Выскубов был личностью примечательной — 1919 года 
  рождения, он был призван в армию накануне войны и уже в ходе боевых действий 
  оказался бойцом отдельного парашютного батальона Кавказского, затем Крымского 
  фронта. Этот батальон был создан по инициативе начальника разведотдела фронта 
  В.М. Капалкина. Когда в январе 1942 года развернулись военные действия в Крыму, 
  бойцов отдельного батальона начали сбрасывать на самые опасные участки новообразованного 
  фронта с целью проведения разведки и диверсий. Сам Выскубов в районе Арабата 
  участвовал в парашютном десанте, высаженном в тыл немецкой группировки, оборонявшей 
  Керченский полуостров. <br>
  <br>
  <img src="a-zagobelisk3.jpg" width="198" height="141"><br>
  <br>
  Готовя наступательную операцию в Крыму весной 1942 года, командование Крымского 
  фронта решило расширить контакты с партизанами, которые, действуя в тылу врага, 
  служили источником бесценной информации для командования. В марте 1942 года 
  Выскубов был сброшен повторно в Крым в качестве радиста для командования партизанских 
  отрядов Крыма. Благополучно приземлившись, он был прикомандирован к штабу 2-го 
  района партизанских отрядов Крыма, зона ответственности которого простиралась 
  от дороги Симферополь — Алушта до линии Капсихор — Орталан в лесах Восточного 
  Крыма. Главной задачей Выскубова стала передача разведданных из штаба 2-го района 
  на Большую землю. Выскубов пробыл в партизанских отрядах в самый сложный и драматический 
  период 1942—1943 годов, после перерыва снова был сброшен в крымский лес. Награжден 
  орденом Красного Знамени, медалями. В 80-е годы он издал книгу мемуаров, где 
  привел множество подробностей о партизанском движении в Крыму. В этой книге 
  есть и фрагмент, относящийся к деревне Ворон. Приведем дословно воспоминание 
  Выскубова:<br>
  <em><br>
  «…Приняв первую радиограмму, отдаю Григоряну (своему напарнику, тоже радисту 
  — А.М.) ключ и наушники. В радиограмме командующий Крымским фронтом генерал-майор 
  Козлов просит срочно выслать группы на поиск парашютистов-десантников, с которыми 
  потеряна связь. Сообщаются координаты их последнего местонахождения. О десантниках 
  штаб фронта однажды уже сообщал.<strong> Это была группа в двенадцать человек. 
  Выбросили ее с задачей вести наблюдения за Черноморским побережьем. Там, в районе 
  Феодосии и Владиславовки, немцы готовились к решительному наступлению. От Севастополя 
  побережьем перебрасывались туда войска, чтобы внезапным ударом сломить оборону 
  Крымского фронта.</strong> Во второй радиограмме штаб фронта просил срочно сообщить 
  о расположении штаба 11-й немецкой армии и о ее командующем. Генов, прочитав 
  радиограммы, тяжело вздохнул и тихо сказал: — О Манштейне-то мы знаем, где он 
  находится со своим штабом. А вот что касается парашютистов!.. — и, помолчав, 
  добавил: — Нету их больше. — Как нету? — вырвалось у меня. Я впился глазами 
  в Генова. — Вы что-то знаете о них? Неужели погибли? — Погибли. Все погибли, 
  — с трудом произнес Генов. — Я только сегодня узнал о них: разведчик принес 
  эту весть, — он замолчал, уткнулся в радиограмму, но, видимо, не читал ее, думал. 
  Может, о парашютистах, а может, о чем другом... — Они что, ваши однополчане? 
  — спросил чуть позже.— Да, из нашего батальона, — сказал я, продолжая сверлить 
  Генова глазами: ждал от него подробностей о гибели моих друзей. Наконец Генов 
  с сочувствием посмотрел на меня и тихо произнес: — Погибли ребята. Что ж теперь 
  делать? Война есть война. Без жертв она не бывает. <strong>Так вот, по рассказам 
  разведчика, десантники благополучно приземлились в нужном районе. Какое задание 
  имели, нам неизвестно. Они уже пробивались к Старокрымскому лесу: видимо, шли 
  на соединение с партизанами. Поздно вечером зашли в деревню Ворон, постучали 
  в крайний дом. Их впустили. Но хозяин оказался предателем: поднял на ноги местных 
