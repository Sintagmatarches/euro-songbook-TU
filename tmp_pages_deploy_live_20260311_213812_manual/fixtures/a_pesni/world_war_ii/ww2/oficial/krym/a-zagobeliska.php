  документ, он лишь воспроизводит какие-то донесения конца января 1942 года. Какие? 
  Село Ворон находилось в зоне ответственности Второго партизанского района, поэтому 
  искать стоит прежде всего в документах штаба И.Г. Генова. Перелистываем рукописные 
  листки текстов радиообмена штаба Второго района со штабом Фронта — и вот перед 
  нами текст радиограммы, отосланной 28 января 1942 года. Она является ответом 
  на отправленную в лес за два дня до этого радиограмму Крымского фронта с просьбой 
  сообщить сведения о трех разведгруппах, сброшенных в тыл немцев, и о судьбе 
  судакского десанта. Командир отрядов Второго района И.Г. Генов и комиссар района 
  Е.А. Попов сообщают начальнику Разведотдела Фронта Капалкину следующее:<em> 
  «Группа 8 парашютистов блудили 6 дней [в] лесу. 19 [января] зашли [в] дер[евню] 
  Ворон. Вооруженные татары напали, сутки бойцы героически от[стреливались]. Фашистские 
  бандиты сожгли дом с бойцами»</em> [14]. Переворачиваем еще несколько листков 
  — перед нами, наконец, исходный документ, первым зафиксировавший правду о происшедшем 
  в Вороне. Это донесение командира первого района партизанских отрядов Крыма 
  А.А. Сацука, адресованное замкомадующему партизанскими отрядами Крыма, начальнику 
  Второго района партизанских отрядов Крыма И.Г. Генову. О значении, которое придавали 
  этому документу его составители, говорит тот факт, что в отличие от большинства 
  других партизанских документов, обычно написанных от руки простым карандашом, 
  этот отпечатан на печатной машинке и подписан не только лично Сацуком, но и 
  комиссаром рай-она М.С. Вялковым и начальником Особого отдела района Валиулиным. 
  Это вполне понятно: донесение шло в центр — в особый отдел фронта, который всю 
  вторую половину января буквально бомбил штаб Второго района, где была единственная 
  в восточной части Крыма рация, с требованиями дать сведения о пропавших разведгруппах. 
  Дата документа не читается, часть листа немного повреждена, но по содержанию 
  он относится к последним дням января 1942 года. Вот что доносили партизанские 
  командиры Первого района (документ приводится с сохранением особенностей языка 
  документа, сокращения обозначены отточиями):<br>
  <br>
  <em>Командиру партизанских отрядов 2 района<br>
  тов. Генову,<br>
  Копия — нач.ОО<br>
  [особого отдела] штаба<br>
  тов. Казакову.<br>
  <br>
  По уточненным агентурным данным 19.1.42 г. в д. Ворон вашего района явились 
  8 красноармейцев, которые сказали, что они советские парашютисты и 6 дней блуждают 
  в лесах, ничего не ели.<br>
  <br>
  Указанные красноармейцы остановились в д. Ворон в крайнем доме (дом расположен 
  под скалой). Фамилия владельца дома не установлена {…}. Как только красноармейцы 
  зашли в дом этого гражданина и объяснили свое положение, тут же хазяин дома 
  что-то сказал своим девочкам и они ушли. Вскоре дом этого гражданина был окружен 
  местным населением до 300 чел., многие из местных жителей были вооружены винтовками 
  и полуавтоматом. Как только дом был окружен, хозяин дома со своей женой вышли, 
  с уходом их из дома местное население стало обстреливать дом. Красноармейцы, 
  находившиеся в доме, пытались объясниться, но местное население их не слушало 
  и до вечера дом обстреливали. В тот день был убит один красноармеец и один человек 
  из местного населения. Местное население целую ночь не уходило, к утру староста 
  дер. Ворон Кара Нури послал представителей по дер. Ай-Серез, Капсихор, Кутлак 
  и Шелен за помошью. В это время за исключением д. Кутлак немецких войск не оказалось. 
  Немецкий офицер, находившийся со свой группой в Кутлаке сказал: «Что хотите 
  с этими красноармейцами делайте, нам некогда». (16.1 с. г. советским десантом 
  был занят г. Судак).<br>
  <br>
  К утру 20.1 с. г. в д. Ворон собрались из д. Айсерез 14 чел. татар, из д. Шелен 
  9 чел. татар и из д. Капсихор 12 чел. татар. Когда все собрались вокруг дома, 
  стали обсуждать, каким образом уничтожить этих красноармейцев. Решили разрушить 
  дом, но когда приступили к разрушению дома, то находившиеся красноармейцы в 
  доме открыли ураганный огонь, осаждающие отступили.<br>
  <br>
  После этого по предложению представителя из д. Капсихор — Чандри Усеина (бывший 
  реэмигрант и турецко-подданный) решили дом сжечь, о чем договорились с владельцем 
  дома оплатить ему все расходы обществом. В течение нескольких минут воронские 
  жители приносили из своих домов керосин, бензин, тряпье и сожгли дом. Красноармейцы, 
  борясь героически, отстреливались и сгорели. После того как дом сгорел, староста 
  д. Ворон Кара Нури и другие зашли в дом, при осмотре установили 3 сгоревших 
  трупа и в одном из сундуков нашли 4 трупа {...}»</em> [15].<br>
  <br>
  Дальше авторы донесения привели фамилии тех, кто наиболее активно участвовал 
  в уничтожении десантников, и просили командование Второго района, поскольку 
  Ворон находился в зоне его ответственности, «принять меры к уничтожению этих 
  предателей» [16].<br>
  <br>
  Итак, воронская трагедия предстает перед нами во всех своих подробностях, которые 
  не подтверждают популярные литературные и легендарные версии 60—70-х годов. 
  Понять причину возникшей путаницы несложно: Крымский партархив, где хранится 
  собрание партизанских документов, в том числе документы, имевшие отношение к 
  военной разведке, в то время когда воронский энтузиаст Петрунин начал свои разыскания, 
  простым смертным был недоступен. Поскольку событие в Вороне хронологически совпало 
  с судакским десантом, Петрунин сделал логичное (хотя и неверное) предположение, 
  будто погибшие здесь десантники являлись частью Судакского десанта — моряками. 
  В этом направлении он и продолжал поиски. Запросы в архивы подтвердили, что 
  в районе Судака в январе 1942 года действительно погиб (или пропал без вести) 
  целый ряд моряков-разведчиков Черноморского флота, их фамилии, надо думать, 
  не без труда, через архивы в конце концов установил Петрунин и высек на могильной 
  плите. Остальное «доделали» журналисты. Л. Литвинова посчитала, что воронская 
  история не будет достаточно патриотичной без пения гибнущими бойцами «Интернационала», 
  а Корабельский «нашел» даже живого очевидца трагедии, с которым наверняка произошла 
  подобная история, только не имевшая отношение к Ворону.<br>
  <br>
  Значит, под плитой воронского обелиска покоятся останки вовсе не тех, чьи имена 
  начертаны на его плите? Не будем спешить с выводами — в этой истории, похоже, 
  один «сюрприз» сразу следует за другим.<br>
  <br>
  Ни один из аутентичных документов не приводит фамилий погибших в Вороне парашютистов 
  — это также результат более позднего расследования, произведенного архивистом 
  И.П. Кондрановым. Вывод о том, что погибшие в Вороне бойцы были членами развед-группы 
  младшего сержанта К.П. Юргенсона, он сделал на основании «Списков диверсионных 
  и разведовательных групп, заброшенных в Крым, радистов партизанских соединений, 
  агентуры, работавшей на оперативные органы партизан». Этот документ допополнялся 
  в течение нескольких лет. Он включает несколько десятков фамилий военнослужащих, 
  посылавшихся в партизанские отряды в период боевых действий в Крыму. Большинство 
  фигурантов этого списка погибли или пропали без вести, особенно те, кого сбрасывали 
  в лес в начальный период войны. В этом списке значится и группа младшего сержанта 
  Карла Петровича Юргенсона в составе 12 человек. Все они обозначены как «пропавшие 
  без вести», но в этой графе напротив фамилии Юргенсона есть приписка, сделанная 
  в начале 1942 года от руки: <em>«(имеются сведения — были сожжены в дер. неизвестной 
  Старокрымского р-на 11.01.42)»</em> [17]. На основании этого указания Кондранов 
  и сделал вывод о том, что в Вороне погибли парашютисты Юргенсона. Эта приписка 
  ставит сразу же два вопроса: первый — Старокрымского района в Крыму не было, 
  село Ворон находилось в Судакском районе, т. е. по другую сторону горного хребта, 
  довольно далеко от Старого Крыма, и второй — событие в Вороне произошло 19—20 
  января, а здесь — одиннадцатое... Кроме того, еще две «нестыковки»: в группе 
  Юргенсона было 12 бойцов, в Вороне же погибли 7 или 8 человек. Это обстоятельство 
  было отмечено уже Геновым — внизу донесения Сацука он сделал приписку своей 
  рукой: <em>«бойцов было не восемь, а одиннадцать»</em> [18] (хотя на самом деле 
  высадилось 12 человек). Конечно, остальные могли потеряться при выброске, заблудиться, 
  но как быть со следующим: радиограмма Генова о пропавшей разведгруппе была датирована 
  28 января 1942 года, С.Н. Выскубов же был сброшен к партизанам только в марте. 
  Таким образом, он никак не мог ни отправить ее, ни быть очевидцем партизанских 
  поисков группы Юргенсона. Не идет ли речь во всех этих документах и воспоминаниях 
  о разных разведгруппах, о разных событиях?<br>
  <br>
  И вот, словно в подтверджение этой догадки, перед нами оказывается еще один 
  партизанский документ — выдержка из отчета бойца-разведчика Судакского партизанского 
  отряда Э. Юсуфова, (крымский татарин, бывший директор судакской МТС и командир 
  судакского партизанского отряда, геройски погиб в конце 1942 года). Она помещена 
  в большой подборке документов, составленных Крымским обкомом ВКП(б) для характеристики 
  ситуации в «татарском вопросе». Татарский вопрос и партизанское движение в Крыму 
  — это отдельная сложная тема, желающих познакомиться с ней ближе я отсылаю <a href="a-partizkrym.php">к 
  своей статье</a> в журнале «Историческое наследие Крыма», № 14. Здесь же нас интересует 
  лишь одна фраза бывшего командира судакского отряда. Говоря о поведении соплеменников 
  в период высадки десантов в начале 1942 года, он сообщил следующее:<em> «До 
  последнего времени деревни Ворон, Ай-Серез, Шелен ведут себя против партизан, 
  устраивают засады на дорогах и в лесу и [являются] самыми активными разведчиками. 
  В Шелене жгли парашютистов, в Вороне жгли в январе 12 красноармейцев из морского 
  десанта, когда жгли этих красноармейцев, участвовали люди из дер. Ворон, Шелен, 
  Капсихор, Ай-Серез. Население этих деревень при встрече с десантниками и партизанами 
  в лесу сразу заявляло, чтобы те сдавались в плен»</em> [19]. Согласно Юсуфову, 
  а он был весьма осведомленным человеком, в период десантов имел место не один, 
  а два разных случая — в Шелене с парашютистами, в Вороне с бойцами морского 
  десанта. Шелен (ныне Громовка) находится всего в трех километрах от Ворона, 
  их разделяет лишь невысокий горный хребет, да и деревеньки эти похожи как близнецы 
  — обе в красивых речных долинах, среди скал. Что же — напутала партизанская 
  разведка Сацука? Врядли, слишком обстоятельно составлено его донесение Генову, 
  скорее всего ошибся Юсуфов — в Вороне погибли все-таки парашютисты, а в Шелене 
  красноармейцы морского десанта... Но тогда закономерен вопрос — а не было ли 
  где-то поблизости других подобных случаев? И не погибла ли группа Юргенсона 
  действительно в неизвестной деревне «Старокрымского района» за девять дней до 
  того, как приблизительно то же произошло с неизвестными бойцами в Вороне? В 
  2006 году в московском издательстве «Прометей» вышла книга В.Г. Науменко «Просто 
  фронт. О морском десанте у феодосийских берегов» — публицистическое повествование 
  о малоизвестных эпизодах Керченско-феодосийской десантной операции, написанное 
  с привлечением большого количества архивных материалов и воспоминаний участников 
  событий. Так вот, автор приводит факт гибели в январе 1942 года у деревень Покровка, 
  Петровка, Кулечь-Мечеть, Сеит-Эли (ныне Журавка) нескольких групп советских 
  разведчиков, одна из которых из 814-го сп 236-й сд 44-й армии, посланная в разведку 
  в район деревни Кулечь-Мечеть, якобы была захвачена в плен, бойцы жестоко убиты 
  и сожжены. Аналогичный случай, согласно данным Науменко, имел место и в деревне 
  Сеит-Эли [20]. Эти события произошли в промежутке между 2 и 15 января 1942 года 
  в непосредственной близости от города Старый Крым.<br>
  <br>
  Последующее исследование документов свидетельствует, что в хаосе начала 1942 
  года подобное тому, что произошло с группой Юргенсона, очевидно, происходило 
  и с другими парашютными разведгруппами, разведгруппами полевых подразделений, 
  а также с подразделениями морских десантов. Начальник разведотдела фронта Капалкин 
  настойчиво требует от Генова искать разведгруппы Юргенсона, Китаина, Иванова. 
  Из них нашлась только часть группы Иванова, остальные пропали без вести. Один 
  из руководителей партизанского движения в Крыму — Н.Д. Луговой — в воспоминаниях 
  писал: <em>«Тридцать человек… сбросили под Симферополь. Приземление оказалось 
  неудачным — прямо на села салгирской долины…»</em> [21]. В уже упоминавшемся 
  нами «Cписке диверсионных и разведгрупп…» бойцы группы Юргенсона значатся под 
  № 74—85, за ними следует список группы сержанта С.М. Манучарова № 86—100 с указанием 
  в графе «сведения» — «данных нет». Радист Коваленко согласно партизанскому донесению 
  был схвачен добровольцами-татарами и уведен в Ускут. В Шелене погиб сброшенный 
  с парашютом начальник разведки 2-го партизанского района. Разрозненные группы 
  участников морских десантов, также выходя из окружения, в татарских селах попадали 
  в засады и гибли. Вполне вероятно, что в Вороне погибла именно часть группы 
  Юргенсона, но с не меньшей вероятностью можно утверждать, что она погибла в 
  каком-либо другом крымском селе. Воронская история не была единичной, а в том 
  или ином виде, в отдельных деталях повторялась в разных местах и в разное время. 
  Впоследствии в памяти людей эти случаи перемешивались, сливались или, наоборот, 
  разделялись во времени, сведения о них, уже искаженные, попадали в документы, 
  в позднейшие мемуары, обрастали вымышленными деталями, а сами эти события становились 
  легендами...<br>
  <br>
  ***<br>
  <br>
  Итак, перед нами нечто большее, чем просто трагический эпизод из истории Керченско-феодосийской 
  десантной операции, — в этом факте, как в капле воды, отразилась вся драма крымских 
  событий в январе 1942 года. Этот месяц будто выпал из истории крупнейшей за 
  весь период Великой Отечественной войны советской десантной операции. В литературе 
  историю последней принято отсчитывать с 29 декабря 1941-го по 2 января 1942 
  года, заканчивая моментом, когда высадка была завершена и советское наступление 
  на запад Крымского полуострова остановилось [22]. Однако, если считать эту операцию 
  тем, чем она была в действительности, а именно операцией по освобождению Крыма, 
  то ее реальным рубежом следует называть 20-е числа января. Тогда советско-германский 
  фронт на своем крайнем южном участке стабилизировался в районе Ак-Монайского 
  перешейка. Вот этот месяц, когда войсками 44-й армии после исключительно тяжелых 
  боев были оставлены Феодосия и Судак, и выпал из военной истории Крыма — о происходившем 
  в это время на фронте в доступной литературе остались лишь фрагментарные сведения 
  [23]. Между тем эта исключительно яркая и трагичная страница истории войны, 
  как бы предвосхищающая трагедию Крымского фронта в мае 1942 года, настоятельно 
  требует своего изучения.<br>
  <br>
  Наиболее полный и до настоящего времени авторитетный труд о событиях Великой 
  Отечественной войны в Крыму, принадлежащий перу А.Я. Басова, содержит лишь основной 
  абрис событий, приведших к поражению советских войск под Феодосией. Совсем недавно 
  предпринята еще одна попытка разобраться в том, что происходило в Восточном 
  Крыму в интересующее нас время, — это упоминавшаяся уже книга В. Науменко, которая, 
  хотя и не является историческим исследованием в полной мере, все же приводит 
  значительное количество ранее не публиковавшегося и недоступного исследователям 
  архивного материала различных российских собраний — ЦАМО, ЦГА ВМФ, РГАСПИ, РГА 
  КДФ.<br>
  <br>
  Что же происходило в районе Феодосии после 2 января 1942 года? Советские войска 
  44-й и 51-й армий были остановлены на рубеже Киет — Новопокровка — Коктебель. 
  Сам по себе факт этой остановки не объяснен до настоящего времени. А.Я. Басов 
  утверждает, что Манштейну к 1 января удалось сосредоточить в этом районе две 
  немецкие пехотные дивизии и румынский горнострелковый корпус [24]. Но это было 
  не так, упомянутые соединения подошли к названным рубежам лишь неделю спустя. 
  В первых числах января советское наступление здесь сдерживали вырвавшиеся с 
  Керченского полуострова и потерявшие при этом всю тяжелую технику части 46-й 
  пехотной дивизии, 213-й пп 73-й дивизии, 4-я и 8-я румынские бригады из состава 
  горнострелкового корпуса. Это был не столько фронт, сколько заслон, советские 
  войска явно превосходили противника по силе (с 26 по 31 декабря в Феодосию были 
  высажены части 157-й и 236 сд, 9-я и 63-я гсд. Всего к 1 января было высажено 
  в Феодосии 23 000 человек, 34 танка, 133 орудия и миномета). Тем не менее, выйдя 
  на упомянутый рубеж, они не предпринимали энергичных наступательных действий. 
  Это было не понятно Манштейну, который в своих мемуарах написал, что его «слабый 
  фронт под Феодосией» держался «каким-то чудом» [25]. «Чудо», однако, объяснялось 
  тем, что, не имея достоверных разведданных о противнике, советское командование 
  медлило с его решительным преследованием и предпочитало накапливать силы для 
  нанесения «решающего удара». Накопление происходило медленно, вторые эшелоны 
  и тылы из-за непогоды и плохой организации их перевозки морем находились на 
  значительном расстоянии от передовых частей, которые испытывали острую нехватку 
  горючего и боеприпасов. Кроме того, в воздухе господствовала немецкая авиация, 
  наносившая большой урон нашим войскам, ввиду отсутствия у последних средств 
  ПВО. Согласно плану советского командования, дальнейшее наступление в глубь 
  Крыма должно было начаться лишь с выходом, высадившегося под Керчью 51-й армии 
  на Ак-Монайские позиции. В итоге «гонку за время», как назвал это соперничество 
  П. Карель, явно выигрывал Манштейн. Нельзя сказать, чтобы командование Кавказским 
  фронтом не отдавало себе отчета в сложности ситуации. План нового наступления 
  был утвержден уже 2 января. Согласно ему, 51-я армия должна была наступать в 
  направлении Джанкой — Чонгар — Прекоп, а 44-я армия — частью сил на Симферополь, 
  а частью — вдоль Южного берега с одновременной высадкой десантов в Алуште, Ялте, 
  Евпатории. Одновременно в наступление должны были перейти войска Севастопольского 
  оборонительного района. Однако реальных возможностей для его осуществления не 
  было, как и опыта в организации столь сложного взаимодействия частей. В итоге 
  начало общего наступления все время откладывалось, подразделения вводились в 
  бой по частям, в результате несли большие потери и гибли. 4 января командующий 
  Кавказским фронтом Т.Д. Козлов потребовал от командования СОРа перейти в наступление 
  по всему фронту, не считаясь с тем, что его войска находились в оборонительных 
  порядках и требовали перегруппировки, в результате наступление превратилось 
  в серию разрозненных атак, где советские войска несли большие потери. 5 января 
  в Евпаторию из Севастополя был высажен морской десант, полностью погибший через 
  двое суток. 6 января в Судак (Новый Свет) высажена десантная группа из состава 
  226-й гсп 63-й гсд в количестве 218 человек, которая после боя с частями противника, 
  потеряв командира, частично погибла, частично перешла к партизанам, значительная 
  же часть сдалась в плен. Но ни 5-го, ни 6 января общее наступление так и не 
  началось. Лишь 8 января командующий фронтом отдал директиву на общее наступление 
  войск 44-й и 51-й армий и войск СОРа, которое должно было начаться 12-го числа. 
  Но и в этот день оно не началось по тем же причинам. 12 января в 17.30 Ставка 
  Верховного Главнокомандующего лишь утвердила представленный ей план Кавказского 
  фронта по освобождению Крыма и... разрешила перенести начало наступления на 
  16 января. По-видимому, именно готовясь к этому наступлению, командование фронта, 
  с целью сбора необходимых разведданных, выбросило в ближайший немецкий тыл целую 
  серию разведгрупп, среди которых находилась и группа младшего сержанта Юргенсона. 
 <br>
 <br>
