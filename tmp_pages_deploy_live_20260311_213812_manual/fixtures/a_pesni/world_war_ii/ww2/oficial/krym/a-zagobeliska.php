  <br>
  Однако за прошедшие две недели, обстановка в районе взаимодействия частей Кавказского 
  фронта и 11-й немецкой армии в Восточном Крыму радикальным образом изменилась. 
  Пользуясь пассивностью наступавших советских войск, Манштейн сумел сосредоточить 
  в районе Феодосии 30-й и 42-й армейские корпуса и румынскую горнострелковую 
  бригаду. Несмотря на то что советские войска имели численное превосходство, 
  Манштейн решил нанести контудар. Как впоследствии вспоминал командующий 11-й 
  армией, «нелегко было... решиться на это наступление. Оно должно было вестись 
  тремя с половиной немецкими двизиями и одной румынской горной бригадой против 
  противника, силы которого возросли теперь до восьми дивизий и двух бригад. В 
  то время как противник располагал танками, хотя и в ограниченном количестве, 
  у нас не было ни одного. Поддержка авиации стояла под вопросом из-за нелетной 
  погоды» [26]. Итак, в середине января две группировки стояли друг против друга, 
  изготовившись к решающему броску... <br>
  <br>
  <img src="a-zagobelisk6.jpg" width="198" height="270"><br>
  <br>
  Согласно замыслу Манштейна, его ударная группа наносила удар из района Владиславовки 
  на Дальние и Ближние Камыши встык частям 51-й и 44-й армий и должна была выйти 
  к морю несколько восточнее Феодосии, окружив в созданном котле оборонявшие ее 
  части. Опасения командующего насчет погоды не оправдались — он сполна использовал 
  превосходство в авиации, которое оказалось решающим. Данные о начале сражения 
  в немецких и советских материалах разнятся. Манштейн утверждает, что начал свое 
  наступление первым 15 января 1942 года [27], согласно очерку А.Я. Басова, его 
  войска атаковали части, стоявшие в районе Феодосии, утром 16 января [28]. Эта 
  рассогласованность отразила тот факт, что советское командование не вполне понимало 
  происходящее и не отреагировало вовремя на начало немецкого наступления. Об 
  этом же свидетельствует и история высадки советского десанта в Судаке. Согласно 
  позднейшей литературе, этот десант в составе основной части усиленного 226-го 
  сп под командованием майора Н.Г. Селихова, часть которого уже высаживалась под 
  Судаком 6 января, должен был создать угрозу с тыла нацеленной на Феодосию немецкой 
  группировке [29] и тем самым если не сорвать, то ослабить немецкое наступление. 
  Однако, когда он был высажен, ночью с 15 на 16 января и ранним утром, немецкое 
  наступление уже сутки успешно развивалось. Судя по документам, его задача была 
  иной — стать одним из передовых отрядов планировавшегося на 16 января советского 
  наступления. Как явствует из донесения командира 226-го сп майора Н.Г. Селихова, 
  он должен был захватить Алушту, но из-за неблагоприятных погодных условий эта 
  задача решена не была, и 14 января он получил новый приказ — овладеть Судаком 
  [30]. Скорее всего, мысль переориентировать десант и использовать его для отвлечения 
  немецких сил от Феодосии возникла у командования фронтом в самый последний момент. 
  15 января в 23 ч. 45 мин 15 января отряд кораблей ЧФ во главе с линкором «Парижская 
  Коммуна» под общим командованием командующего эскадрой контр-адмирала Л.А. Владимирского 
  подошел к Судаку. Линкор и два эсминца открыли огонь по месту высадки — судакскому 
  пляжу. В то же время с эсминцев «Сообразительный» и «Шаумян» без противодействия 
  были высажены по одной роте на флангах участка высадки в районе Нового Света 
  и восточнее мыса Алчак. 16 января к 6 часам 16 января с крейсера «Красный Крым» 
  и канлодки «Красный Аджаристан» с помощью шести «малых охотников» на пляж Судака 
  высадились основные силы полка (1276 чел.) с 4-мя горными 76-мм орудиями [31]. 
  К 7 утра Судак был очищен от противника.<br>
  <br>
  На ситуацию в районе Феодосии эта высадка никакого влияния не оказала и не могла 
  оказать, поскольку полк, рассчитанный лишь на захват плацдарма до подхода основных 
  сил, не был снабжен необходимыми средствами для наступления. Через два часа 
  после высадки в Судаке войска противника после мощной артподготовки продолжили 
  наступление на Феодосию. Острие удара пришлось на позиции 236-й сд. Как указывает 
  А.Я. Басов, «оборона войск 44-й армии, готовившихся к наступлению, была слабая. 
  Здесь могли поработать тяжелые корабли, но они разрядились при высадке десанта 
  в Судаке. Оборона 236-й сд была прорвана» [32]. Была еще одна причина того, 
  что войскам 44-й армии не удалось организовать оборону, — уже в начале немецкого 
  наступления армия оказалась обезглавленной. Во время налета немецкой авиации 
  бомба разорвалась в непосредственной близости от штаба армии, в результате командующий 
  генерал-майор А.Н. Первушин был тяжело ранен, член военного совета убит, начальник 
  штаба ранен; в командование армией вступил командующий 9-м стрелковым корпусом 
  генерал И.Ф. Дашичев, однако связь между войсками и управление ими были потеряны. 
  И снова разница в дате, теперь уже у очевидцев и позднейших историков с нашей 
  стороны. В. Науменко в своей работе называет точное время происшедшего: 11 ч. 
  15 мин 15 января 1942 года [33], однако в официальной истории этой операции 
  — вновь 16-е [34], т. е. тот самый день, когда немецкое наступление вдруг «обнаружило» 
  командование фронтом.<br>
  <br>
  В образовавшуюся после разгрома 236-й дивизии генерала Мороза брешь из резерва 
  была брошена 63-я гсд полковника Цендзеевского, однако она лишь на некоторое 
  время задержала противника, но и ее части были опрокинуты. 17 января, во второй 
  половине дня, противник овладел Феодосией, практически разгромив две советские 
  дивизии и захватив массу пленных. Советские войска, согласно приказу командующего 
  фронтом, отошли на Ак-монайские позиции. Впоследствии целый ряд участников этих 
  событий ответственность за сдачу Феодосии возлагал на эти самые «политически 
  и морально неустойчивые» 236-ю сд и 63-ю гсд 44-й армии и их командование [35]. 
  Это были дивизии, укомплектованные в основном представителями кавказских народов 
  (63-я — бывшая Грузинская). Проявлявший часто храбрость в боях личный состав 
  этих подразделений был слабо обучен и часто просто не понимал русского языка, 
  на котором отдавались приказы. Эти части еще не участвовали в боях, они были 
  выведены из Ирана и не имели никакого представления о том, что такое современная 
  война. Особенно сильное впечатление на солдат производила немецкая авиация, 
  которая, по словам одного из очевидцев, буквально «деморализовала» войска. Тем 
  не менее, как показала В. Науменко, многие подразделения сражались храбро и 
  были практически полностью уничтожены в боях. Потери (безвозвратные) 63-й и 
  236-й дивизий за период боев с 13 по 20 января 1942 года, по советским данным, 
  составили 16 227 человек (По разным данным, в составе 63-й дивизии после вывода 
  на Ак-Монай оставалось от 40 до 16% личного состава, в 236-й — от 10 до 4,5% 
  [36]). Эти цифры в целом соответствуют приведенным Манштейном в «Утерянных победах»: 
  «Противник потерял, — сообщал он, — 6700 чел. убитыми, 10 000 пленными, 177 
  орудий и 85 танков». Катастрофа под Феодосией стоила жизни командующему 236-й 
  дивизией генерал-майору В.К. Морозу: по приговору Военного трибунала «за сдачу 
  Феодосии» 22 февраля 1942 года он был расстрелян. Командующий 63-й гсд полковник 
  П.Я. Цендзеевский был арестован «за крупные просчеты в руководстве войсками», 
  однако через месяц освобожден и вновь назначен командиром одной из дивизий. 
  Командующий 9-м стрелковым корпусом И.Ф. Дашичев в 1942 году «за преступное 
  руководство войсками и сдачу противнику г. Феодосии» был приговорен к 9 годам 
  лишения свободы, снижен в звании до подполковника, в 1944 году уволен из кадров 
  Советской Армии, в 1953-м реабилитирован [37]. Виновные были названы, тем не 
  менее в обстоятельствах сдачи Феодосии многое и по сей день остается неясным.<br>
  <br>
  19 января 1942 года, когда советские войска за-крепились на Ак-монайских позициях 
  восточнее Феодосии, теперь уже в глубоком тылу немецко-румынских войск продолжали 
  сражаться бойцы Судакского десанта. Еще к вечеру 16 января десантники заняли 
  Судак, Новый Свет, Большой и Малый Таракташ и развернули наступление в сторону 
  Феодосии, стремясь соединиться с Коктебельской группой совет-ских войск. 17 
  января (когда немцы уже взяли Феодосию) полк майора Селихова занял села Токлук, 
  Козы и вышел к деревне Отузы, где завязался напряженный бой, длившийся весь 
  следующий день. 19 января освободившиеся из-под Феодосии немецкие части обрушились 
  на десантников. Как сообщал в своем донесении Селихов, в этот день «противник 
  ввел в бой дальнобойную артиллерию [и] ... перешел в наступление, но был отброшен» 
  [38].<br>
  <br>
  19-го же января в Ворон, находящийся в одном дневном переходе от Отуз, после 
  недельных скитаний спустилась и разведгруппа советских парашютистов...<br>
  <br>
  Драматическая судьба этих подразделений связана с еще одним явлением в истории 
  Крыма в период Великой Отечественной войны — это коллаборационизм значительной 
  части крымскотатарского населения. Тема эта сама по себе сложная и многообразная 
  и не может быть здесь освещена подробно. Мы вкратце остановимся на том влиянии, 
  которое оказало сотрудничество части местного населения с оккупационными властями 
  на судьбу советских подразделений, высаживавшихся в немецкий тыл.<br>
  <br>
  Говоря об отношении местного населения к оккупационным властям, командующий 
  крымской группировкой немецко-румынских войск Э. фон Манштейн в своих мемуарах 
  отметил, что «татары сразу же встали на нашу сторону. Они видели в нас своих 
  освободителей от большевистского ига, тем более что мы уважали их религиозные 
  обычаи. Ко мне прибыла татарская депутация, принесшая фрукты и красивые ткани 
  ручной работы для освободителя татар «Адольфа Эффенди» [39]. Немецкое командование 
  почти сразу воспользовалось этим отношением, начав формировать иррегулярные 
  охранные подразделения, а также разрешив создание органов «национального самоуправления» 
  — мусульманских комитетов. Высадка советских войск на Керченском полуострове 
  и под Феодосией дала новый толчок к использованию оккупантами крымских татар. 
  Как пишет О. Романько, «2 января 1942 г. в отделе разведки 11 немецкой армии 
  состоялось совещание, в ходе которого было заявлено, что Гитлер разрешил призыв 
  добровольцев из числа крымских татар» [40]. Организация была поручена руководству 
  Айнзацгруппы «Д». Перед ее руководителем О. Олендорфом ставилась задача: «Охватить 
  крымских татар, способных служить в армии, для действий на фронте в частях 11-й 
  армии на добровольной основе, а также создать татарские роты самообороны, которые… 
  будут использованы для борьбы с партизанами» [41]. Уже на следующий день выполнение 
  этой задачи начал Симферопольский мусульманский комитет. В результате в течение 
  января 1942 года в 203 населенных пунктах и 5 лагерях военнопленных было набрано 
  9255 человек, из которых 8684 человека, признанных годными к службе, были направлены 
  в части 11-й армии вермахта. Одновременно 1632 человека, набранных по линии 
  оперативной группы «Д», были сведены в 14 рот самообороны для борьбы с партизанами 
  [42]. Как явствует из целого ряда документов, коллаборационисты приняли самое 
  активное участие в борьбе с десантировавшимися советскими частями. Согласно 
  донесению упоминавшегося уже партизана, бывшего командира Судакского партизанского 
  отряда Э. Юсуфова, «высадку десанта в Новый Свет (6 января 1942 года. — А.М.) 
  заметили кутлакские люди [43], которые заявили об этом в немецкий штаб и в уничтожении 
  этого десанта принимали самое активное участие. Были случаи, когда пойманных 
  красноармейцев раздевали догола, а в Таракташе один татарин убил краснофлотца 
  и одежду взял себе» [44]. Командир 226-го полка майор Селихов сообщал, что 18 
  января к 9.00 в разгар боя у деревни Отузы «предатели-татары вывели обходными 
  тропами на левый фланг и штаб полка группу автоматчиков с минометом, в количестве 
  120 чел., имея задачу окружить и уничтожить штаб полка» [45]. Из немецких документов 
  известно, что только что сформированные из крымских татар подразделения приняли 
  самое активное участие в борьбе с судакским десантом, понеся при этом большие 
  потери — до 400 человек убитыми и ранеными [46]. Уничтожение разведгруппы в 
  Вороне — целиком на совести коллаборационистов из деревень Ворон, Шелен, Ай-Серез 
  и Капсихор. Конечно, и это следует особо подчеркнуть, не все крымские татары 
  из ближайшего к театру боевых действий района примкнули к коллаборационистам. 
  Хорошее отношение к десантникам было замечено среди населения деревни Козы [47] 
  (партизанам помогали жители деревень Шахмурза, Арматлук, Имарет). В партизанских 
  отрядах 1-го и 2-го районов сражались и крымские татары, некоторые из них геройски 
  погибли [48]. Вывод остатков войск судакского десанта к партизанам осуществляли 
  бойцы-проводники судакского отряда Эмиралиев и Мамутов. Тем не менее коллаборационисты 
  оказали известное влияние на обстановку в районе высадок советских десантов, 
  свидетельством чему и является воронская история. Неэффективные в борьбе с более 
  или менее крупными подразделениями советских войск карательные отряды, созданные 
  из местного населения, отличились, однако, в истреблении мелких разведгрупп, 
  а также отбившихся от своих частей и попавших в окружение красноармейцев.<br>
  <br>
  Эпилог драмы, разыгрывавшейся под Судаком, затянулся еще на десять дней после 
  того, как основное сражение у Феодосии советскими войсками было проиграно. С 
  19 по 22 января 226-й полк вел упорные бои в районе развилки дорог Отузы — Козы, 
  горы Токлук-Сырт и перевала Синор, в которых противник активно использовал авиацию. 
  В ночь с 23 на 24 января полк отошел и закрепился восточнее Большого Таракташа. 
  Здесь ему пришло неожиданное подкрепление в виде 554-го сп майора Забродоцкого, 
  высаженного с кораблей Черноморского флота в ночь с 25 на 26 января. В чрезвычайно 
  сложных условиях, в разгар шторма, в Судак были высажены еще 1326 десантников 
  (непогода не позволила высадить еще 250 чел.), одновременно кораблям удалось 
  забрать две сотни раненых из 226-го полка. Появление в районе Судакского плацдарма 
  нового подразделения, в то время как обстановка требовала скорее эвакуации остатков 
  полка Селихова, до сих пор вызывает недоумение. Это тоже одно из не вполне понятных, 
  хотя и объяснимых решений командования Кавказского фронта, которых принималось 
  много в январе 1942 года.<br>
  <br>
  Дело в том, что оставление Феодосии — важного для дальнейшего наступления порта 
  — вызвало большое раздражение в Ставке Верховного Главнокомандующего. В разговоре 
  с командующим Кавказским фронтом Т.Д. Козловым Сталин требовал организовать 
  контрнаступление и отбить Феодосию. Выполняя требование Ставки, командование 
  фронтом пыталось предпринять какие-то наступательные действия. Поскольку судакский 
  десант еще держался, его, судя по всему, решили усилить и использовать захваченный 
  им плацдарм для создания угрозы с тыла феодосийской группировке противника, 
  однако, как это происходило не раз, наступление фронтовых частей так и не началось. 
  Разразившийся шторм и налеты немецкой авиации сделали эвакуацию десанта морем 
  невозможной. 26 января развязался последний бой в районе Судака. Согласно донесению 
  майора Селихова, его позиции были атакованы силами двух немецких полков пехоты, 
  частями 4-й румынской бригады при поддержке тяжелой артиллерии и авиации (Селихов 
  также сообщал о танках) [49]. Бой длился весь день, и его исход был предрешен. 
  В этих условиях Селихов принял решение с остатками своих частей перейти к партизанам, 
  с которыми он все время боев поддерживал тесную связь. 27 января, к вечеру, 
  Судак был оставлен, и остатки полков перешли в район горы Сугут-Оба в расположение 
  штаба 1-го района партизанских отрядов Крыма. В ротах к этому времени оставалось 
  до 15% личного состава, всего у партизан оказалось до 500 десантников, свыше 
  700 человек раненых бойцов оставались в Судаке на морском берегу в тщетном ожидании 
  подхода каких-либо плавсредств для эвакуации. По сообщению партизанских источников, 
  все они были убиты ворвавшимися в Судак карателями [50].<br>
