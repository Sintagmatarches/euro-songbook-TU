	Слова и музыка написаны в 1942 г. В годы Великой Отечественной войны песня была очень популярна, 
  на ее мелодию на фронте было создано множество новых песен, самая известная 
  из которых - <a href="zastolnvolh.php">&quot;Застольная Волховского фронта&quot;</a> на слова Павла Шубина (там же см. ноты). Мелодия песни &quot;Наш тост&quot; 
  - одна из наиболее часто используемых в фольклоре Великой Отечественной.<br>
  <br>
  Автором текста в разных изданиях упоминаются либо Матвей Косенко и Арсений Тарковский вместе, либо один из них. Тарковский на момент создания песни был военным корреспондентом газеты 16-й армии «Боевая тревога».

 Текст написан по приказу командующего фронтом генерала Баграмяна, авторское его название - «Гвардейская застольная».<br>
 <br>
 После развенчания культа личности Сталина его имя из песни было удалено.<br>
