	<strong>Александр Петрушин </strong><br>
  <br>
<span class="header"><strong>ГАЗОВАЯ ИСТОРИЯ</strong><br></span>
  <br>
  <em>«Тюменский курьер», 29 февраля 2008, №35(2327).</em><br>
  <br>
  <br>
  <strong><em>Причиной очередного газового конфликта между Россией и Украиной 
  стали не только разногласия о ценах на энергоносители, но и разные оценки событий 
  XVII-ХХ веков и деятельности таких исторических личностей, как Богдан Хмельницкий, 
  Иван Мазепа и Степан Бандера. <br>
  </em></strong><br>
  <br>
  <strong>«Бандеровщина» </strong><br>
  <br>
  Спор о прошлом Украины начался задолго до обретения ею в сентябре 1991 года 
  «незалежности». Пересмотр советской официальной исторической науки, во многом 
  сфальсифицированной, привел к переименованию украинских городов и улиц, к сносу 
  памятников героям революции, Гражданской и Великой Отечественной войн. Участников 
  вооруженных антисоветских националистических формирований, включая созданный 
  гитлеровцами из украинцев батальон «Нахтигаль» («Соловей») и 14-ю дивизию СС 
  «Галичина», реабилитировали и уравняли в правах с теми, кто воевал на стороне 
  антигитлеровской коалиции. <br>
  <br>
  Чашу терпения Москвы переполнило решение президента Украины Виктора Ющенко, 
  служившего в молодости в погранвойсках КГБ СССР, присвоить звание Героя Украины 
  Роману Шухевичу, командующему Украинской повстанческой армией (УПА), воевавшей 
  с вермахтом, Красной армией и польской Армией Крайовой. Не способствовало сближению 
  России и Украины объявление наступившего года годом Степана Бандеры, одного 
  из идеологов национальной независимости украинского народа. <br>
  <br>
  Степан Андреевич Бандера родился в 1908 году (по другим данным годом позже) 
  в селе Старый Угрынив Станиславской губернии (Ивано-Франковская область) в семье 
  греко-католического священника, который некоторое время служил в петлюровской 
  армии и был депутатом законодательного собрания недолго существовавшей Западно-Украинской 
  народной республики. Мать рано умерла — от туберкулеза. <br>
  <br>
  Бандера окончил польскую гимназию и поступил на агрономический факультет Львовского 
  политехнического института. Его с юности отличали упорство, сильная воля и целеустремленность. 
  Он не пил, не курил, занимался спортом, играл на гитаре, пел в хоре. Легко сходился 
  с людьми и подчинял их своему влиянию. <br>
  <br>
  В 1929 году появилась организация украинских националистов (ОУН). Ее цель — 
  создание украинского государства. Главный враг — поляки, поскольку Западная 
  Украина входила в состав Польши. <br>
  <br>
  Недоучившийся 23-х летний Бандера стал заместителем председателя ОУН полковника 
  Евгена Коновальца. <br>
  <br>
  Летом 1934-го Бандера подготовил в Варшаве убийство министра внутренних дел 
  генерала Бронислава Перацкого в ответ на «злодеяния против украинского народа». 
  Бандеру приговорили к смертной казни, которую заменили пожизненным заключением. 
  <br>
  <br>
  Пока Бандера сидел в тюрьме, восходящая звезда НКВД СССР — будущий генерал-лейтенант 
  Павел Судоплатов — в самом центре Роттердама преподнес Коновальцу коробку любимых 
  им конфет, начиненную вместо шоколада взрывчаткой. <br>
  <br>
  После гибели Коновальца главой ОУН провозгласили обосновавшегося в Италии полковника 
  Андрея Мельника. Но украинская молодежь не захотела подчиняться укрывшимся в 
  Европе эмигрантам и объявила своим вождем Бандеру. <br>
  <br>
  А через год, осенью 1939-го, вермахт и Красная Армия совместными усилиями разгромили 
  Польшу, и немцы выпустили Бандеру из польской тюрьмы на свободу. Он тайно пробрался 
  во Львов, занятый советскими войсками. Этот город был официальным центром Галиции, 
  которая никогда не входила в состав России и исповедовала униатство. <br>
  <br>
  Ускоренная советизация края оттолкнула население от советской власти. Начались 
  массовые высылки недовольных — НКВД Украины под руководством комиссара госбезопасности 
  3-го ранга Ивана Серова изгнал из родных мест почти миллион человек. <br>
  <br>
  Известный кинорежиссер и драматург Александр Довженко с горечью писал в 1940 
  году: «Вообще наши ведут себя плохо: на Западной Украине нас встречали в прошлом 
  году изумительно, а теперь мы довели до того, что там ширится повстанческое 
  движение. Мы несем нашу грубость, неделикатность, некультурность». <br>
  <br>
  ОУН и Бандера оказались единственными защитниками интересов украинского народа. 
  Бандера наивно решил, что только немцы дадут украинцам то, в чем им отказывали 
  поляки и русские, — государственную независимость. <br>
  <br>
  <br>
  <strong>Против всех </strong><br>
  <br>
  После нападения немцев Красная армия оставила Львов. Город оказался во власти 
  вооруженных формирований украинских националистов. ЗО июня 1941 года Бандера 
  провозгласил «Акт возрождения Украинского государства». Однако создание независимой 
  Украины не входило в планы Гитлера. <br>
  <br>
  Бандера был арестован и три года провел в концлагере Заксенхаузен (вместе с 
  сыном Сталина старшим лейтенантом Яковом Джугашвили). Оставшиеся на свободе 
  сторонники Бандеры выступили против немецких оккупантов. В лесах Волыни и Полесья 
  началось формирование украинских боевых отрядов, из которых к осени 1942-го 
  сложилась Украинская повстанческая армия. <br>
  <br>
  УПА вела борьбу с немцами, отрядами польской Армии Крайовой и советскими партизанами. 
  Программные установки ОУН на создание независимого Украинского государства неизбежно 
  вели и к конфронтации с войсками Красной армии, вступившей в конце 1943-го на 
  Правобережье. <br>
  <br>
  Тогда же немцы договорились с Бандерой, что украинские националисты получат 
  оружие для проведения разведывательно-диверсионных операций в тылу советских 
  войск. УПА приняла политическую декларацию, в которой заявлялось: «Мы за полное 
  освобождение украинского народа от московско-большевицкого и немецкого ярма, 
  за создание независимой соборной державы без панов, помещиков, капиталистов, 
  без большевицких комиссаров, энкавэдистов и партийных паразитов». <br>
  <br>
  Против наступающей Красной армии и восстановленных в освобожденных от немцев 
  районах органов советской власти развернулась настоящая партизанская война. 
  УПА контролировала примерно четверть территории Украины. Оуновцам удалось подстеречь 
  командующего 1-м Украинским фронтом генерала армии Николая Ватутина. Он умер 
  в госпитале от ран. Украинские националисты убили легендарного советского разведчика 
  Николая Кузнецова и заместителя министра обороны Польской народной республики 
  генерала брони Кароля Сверчевского, одного из организаторов Войска Польского. 
  <br>
  <br>
  В свою очередь органы госбезопасности и внутренних дел ответили массовым террором. 
  В донесении наркома внутренних дел Украины комиссара госбезопасности 3-го ранга 
  Василия Рясного отмечалось, что на протяжении 1944-1945 годов было проведено 
  40 тысяч операций, в ходе которых убито 103 тысячи и задержано 125 тысяч повстанцев. 
  Поскольку количество бойцов УПА никогда не превышало 80 тысяч, значительную 
  часть убитых и арестованных составили мирные жители края, которые в большинстве 
  сочувствовали идеям оуновцев. <br>
  <br>
  Сам Бандера в конце войны оказался в Кракове и не успел покинуть город до его 
  захвата советскими войсками. Лидера украинских националистов вызволил любимец 
  Гитлера командир спецназа СС штурмбанфюрер Отто Скорцени. На Нюрнбергском процессе 
  он рассказал: <br>
  <br>
  — Это был трудный маршрут. Я вел Бандеру по радиомаякам, оставленным в тылу 
  ваших войск в Чехословакии и Австрии. Нам нужен был Бандера. Мы верили ему. 
  Гитлер приказал мне спасти его, доставить в рейх для продолжения работы. Я выполнил 
  это поручение... <br>
  <br>
  Повстанческим движением на Украине руководил соратник Бандеры генерал-хорунжий 
  Роман Шухевич (псевдоним «Тарас Чупринка»), давний агент Абвера, немецкой военной 
  разведки. Характерно, что всю систему своего пребывания в подполье он поставил 
  на использовании женщин. Но в войне с Советами УПА изначально была обречена 
  на поражение, так как не могла в одиночку одолеть могучее тоталитарное государство. 
  <br>
  <br>
  После окончания Второй мировой войны у Москвы появились возможности наводнить 
  непокорное Правобережье регулярными войсками и организовать широкие карательные 
  операции. В «большой блокаде» 1946 года было задействовано несколько дивизий 
  численностью почти в 60 тысяч человек при поддержке артиллерии, танков и авиации. 
  Репрессиям подвергались все, кто имел хоть малейшее отношение к ОУН или УПА. 
  <br>
  <br>
  Так, военный трибунал войск НКВД Тернопольской области приговорил к 25 годам 
  лишения свободы «Рокецкого Юлиана Львовича, 1913 года рождения, уроженца и жителя 
  с. Носов Подгаецкого района Тернопольской области, крестьянина, беспартийного, 
  с 4-х классным образованием, семейного». Отца будущего губернатора Тюменской 
  области обвинили в том, что «в январе 1946 года его хозяйство посетили бандиты 
  ОУН, с которыми он разговаривал (попробовал бы не поговорить — Авт.) и отдал 
  им военный ремень». Из лагеря Рокецкого-старшего освободили после смерти Сталина 
  в марте 1953-го, когда сыну исполнилось 11 лет. <br>
  <br>
  Несмотря на потери УПА и высылку пособников в Сибирь, включая и Тюменскую область, 
  накал сопротивления Советской власти на Западной Украине не спадал. Всего в 
  результате ответного террора УПА и подполья украинских националистов погибло 
  почти 30 тысяч граждан и до 20 тысяч военнослужащих, милиционеров, чекистов 
  и пограничников. <br>
  <br>
  Но постепенно повстанческое движение пошло на спад. Помогать оуновцам стало 
  страшно: крестьяне боялись давать им приют, кормить, снабжать информацией. 5 
  марта 1950 года спецгруппой МГБ был ликвидирован на подпольной квартире командующий 
  УПА Шухевич. Там же задержали и его ближайшую помощницу Галину Дидик (псевдоним 
  «Анна») — она освобождена из заключения в 1971 году. <br>
  <br>
  <br>
  <strong>«Работа нелегкая, но почетная» </strong><br>
  <br>
  После подавления вооруженного сопротивления на территории Западной Украины Бандера 
  обосновался в Мюнхене с документами на вымышленное имя Стефана Поппеля. Для 
  того, чтобы разыскать его, органам госбезопасности СССР потребовалось несколько 
  лет. <br>
  <br>
  В 1959 году Никита Хрущев одобрил план ликвидации Бандеры, которому Верховный 
  Суд СССР заочно вынес смертный приговор. Некоторые руководители КГБ предупреждали 
  Кремль, что убийство Бандеры не только не принесет пользы, но и вызовет консолидацию 
  оуновцев. Но в ЦК КПСС с этим мнением не согласились. <br>
  <br>
  Исполнителем операции был назначен 25-летний Богдан Сташинский, завербованный 
  МГБ во время учебы в Львовском университете. С его помощью в июне 1953 года 
  чекисты арестовали Романа Щепанского — организатора убийства известного украинского 
  писателя-коммуниста Ярослава Галана. Позднее Сташинский через сестру вошел в 
  доверие к ее жениху, который являлся командиром одного из боевых отрядов ОУН, 
  и ушел с ним в лес. А через некоторое время отряд был уничтожен опергруппой 
  МГБ. <br>
  <br>
  Знание немецкого языка, физические данные и непримиримое отношение к украинским 
  националистам, которое еще больше укрепилось после того, как он стал свидетелем 
  убийств детей, чьи родители симпатизировали советской власти, сделали из Сташинского 
  отличного боевика-нелегала 13-го (диверсионного) отдела КГБ. <br>
  <br>
  9 октября 1957 года в Мюнхене он убил из специально сконструированного для него 
  газового пистолета одного из идеологов ОУН редактора газеты «Украинский самостийник» 
  Льва Ребета. Ядовитый газ при попадании на лицо жертвы повлек за собой остановку 
  сердца. Смерть Ребета ни у кого не вызвала никаких подозрений. Как и предполагали 
  организаторы убийства, медицинское заключение, сделанное после вскрытия, констатировало, 
  что эмигрант-украинец умер от сердечной недостаточности. <br>
  <br>
  В отличие от журналиста Ребета боевик Бандера держался крайне осторожно. Сташинскому 
  долго не удавалось проникнуть в дом Бандеры. Удобный момент представился 15 
  октября 1959 года: в подъезде дома N 7 по Крейтмайрштрассе Сташинский выстрелил 
  ядовитым газом в лицо Бандере, открывавшему дверь своей квартиры. Поначалу, 
  обнаружив труп некого Поппеля, полиция вновь посчитала, что он умер от сердечного 
  приступа. Но когда выяснилось, что под этим именем проживал Бандера, у немецких 
  властей возникли подозрения относительно его смерти. Повторное исследование 
  трупа обнаружило в желудке Бандеры остатки синильной кислоты, а на лице следы 
  разорвавшейся ампулы с ядом. <br>
  <br>
  Тогда, чтобы запутать следствие, КГБ запустил в западную печать слух о том, 
  что Бандеру убили по инициативе министра ФРГ по делам беженцев Теодора Оберлендера, 
  с которым Бандера тесно сотрудничал в годы Второй мировой войны. Кроме этой 
  версии выдвигались и другие. По одной из них Бандера покончил жизнь самоубийством, 
  приняв яд после того, как узнал о сотрудничестве с КГБ своего ближайшего соратника 
  Мирона Матвиейко («Услих»). Но большинство оуоновцев были убеждены, что их шефа 
  ликвидировали советские спецслужбы. В результате смерть Бандеры консолидировала 
  враждебные СССР силы в украинской эмиграции, а его похороны вылились в мощную 
  демонстрацию единства и сплоченности ОУН. Недаром украинская эмигрантская пресса 
  в те дни писала «Бандера умер, но дух его живет». <br>
  <br>
  Несмотря на то, что худшие опасения некоторых разумных руководителей-противников 
  убийств как средства проведения политики подтвердились, зампред КГБ генерал-полковник 
  Петр Ивашутин обратился в ЦК КПСС с ходатайством о награждении Сташинского, 
  в котором отмечал его значительную роль «в мероприятиях по пресечению антисоветской 
  деятельности украинских националистов за границей». Сам Сташинский тоже чувствовал 
  себя героем. В декабре 1959 года председатель КГБ Александр Шелепин вручил ему 
  орден Красного Знамени «за успешное выполнение особо важного задания правительства». 
  <br>
  <br>
  — Работа вас ждет нелегкая, но почетная, — со значением сказал Шелепин. <br>
  <br>
  Очередным заданием специалиста по «мокрым делам» должна была стать ликвидация 
  Радослава Стецкова, являвшегося премьер-министром Украинской Республики в 1941 
  году, а после войны укрывшегося в Западной Германии. Но этим планам КГБ помешала 
  любимая женщина Сташинского — восточная немка Инге Поль, с которой красавец 
  Сташинский, проживавший в ГДР под фамилией Крылов, познакомился... на танцах. 
  Разумеется, была проведена ее проверка, выявившая антисоветские настроения Поль. 
  Чекистское начальство Сташинского пыталось запретить их встречи, но все было 
  напрасно. Более того, влюбленный агент-боевик попросил разрешение на брак, утверждая, 
  что сумеет оказать на невесту нужное идеологическое влияние. В конце концов, 
  тот же Шелепин санкционировал их свадьбу. В апреле 1960 года супруги Крыловы 
  отправились на медовый месяц в Сочи, а по возвращении в Москву их начали готовить 
  к совместной «нелегкой, но почетной работе» на Западе. <br>
  <br>
  Но вскоре выяснилось, что под влиянием жены Сташинский во многом пересмотрел 
  свои взгляды и стал чуть ли не большим антикоммунистом, чем Инге. Весной 1961-го 
  она выехала рожать в Восточный Берлин, где проживали ее родители, а Сташинского 
  оставили в Москве, несмотря на все его просьбы. Руководитель аппарата КГБ в 
  ГДР генерал-майор Александр Коротков на рапорте по вопросу выезда Сташинского 
  в Берлин наложил резолюцию: «Сташинского на Запад выпускать нельзя. Следует 
  создать ему все условия для жизни здесь, построить дачу в любой части Советского 
  Союза по его желанию». Но когда в августе в Москву пришло известие, что только 
  что родившийся сын Сташинского неожиданно умер от воспаления легких, его вынуждены 
  были отпустить на похороны. Сташинского сопровождал его московский куратор подполковник 
  Юрий Александров, который был абсолютно уверен в преданности своего подопечного 
  и не допускал мысли о его измене. Между тем под влиянием смерти сына супруги, 
  оторвавшись от наружного наблюдения, бежали в Западный Берлин. Там Сташинский 
  признался в убийстве Ребета и Бандеры. Учитывая раскаяние, суд приговорил убийцу 
  к восьми годам тюремного заключения, а организатором преступлений назвал КГБ 
  и советское правительство. <br>
  <br>
  В конце 1966 года Сташинский попал под амнистию, вышел на свободу, при помощи 
  американских спецслужб сменил фамилию и документы и вместе с Инге выехал в США, 
  где, возможно, находится до сих пор, скрываясь от мести как бандеровцев, так 
  и ФСБ. <br>
  <br>
  Считается, что широкая международная огласка суда над Сташинским заставила ЦК 
  КПСС и КГБ отказаться от политических убийств за пределами Советского Союза. 
  <br>
  <br>
  На встрече 14 февраля в Москве президентов Путина и Ющенко одним из условий 
  урегулирования газового конфликта и создания новой схемы газовых отношений между 
  Россией и Украиной стало проведение совместного года великого русского писателя 
  украинского происхождения Николая Гоголя и празднование 300-летия Полтавской 
  битвы (интересно, как представят гетмана Мазепу — изменником Петра I или жертвой 
  интриг Карла XII?)<br>
  <br>
  Понятно, что многие в России недовольны героизацией ликвидированных чекистами 
  Шухевича и Бандеры. Непонятно другое: почему, соглашаясь с государственной канонизацией 
  отрекшегося от власти в марте 1917 года и расстрелянного в июле 1918-го уральскими 
  чекистами в Екатеринбурге Николая II Романова (Русская Православная Церковь, 
  как известно, воздержалась от участия в этой церемонии), с перезахоронением 
  в Москве доставленных из США останков белогвардейского генерала Деникина, реабилитацией 
  адмирала Колчака и установкой ему памятника в Иркутске, мы возмущаемся переосмыслением 
  истории украинского народа. <br>
  <br>
  Ведь «бандеровщина» — та же гражданская война, когда у каждой из противоборствующих 
  сторон своя правда. Но не надо поднимать на освободившиеся пьедесталы новых 
  героев, потому что в гражданских войнах нет ни победителей, ни героев. <br>
  <br>
  Есть только жертвы. <br>
