Где б они ни появлялись, <br>
Всюду лица расцветали, <br>
Рвались ноги в пляс лихой. <br>
Вдруг война. Пришла забота, <br>
И четыре патриота, не замедля, стали в строй. <br>
С фланга встал Иван Иваныч, <br>
Рядом с ним - Иван Степаныч, <br>
Третьим был Иван Ильич, <br>
И в конце - Иван Кузьмич. <br>
<br>
Раз у леса возле луга <br>
Собрались четыре друга, чтобы ноты разобрать. <br>
Вдруг глядят - идет машина... <br>
Трубы - в сторону и чинно <br>
Все четыре стали ждать. <br>
За сосной - Иван Иваныч, <br>
За копной - Иван Степаныч, <br>
За кустом - Иван Ильич, <br>
За пеньком - Иван Кузьмич. <br>
<br>
Ждут друзья не без опаски. <br>
Поравнялись - видят каски и немецкие штыки. <br>
Немцы вскрикнули, но поздно - <br>
Всех сразили виртуозно <br>
Музыкальные стрелки. <br>
Одного - Иван Иваныч, <br>
Одного - Иван Степаныч, <br>
Одного - Иван Ильич, <br>
И двоих - Иван Кузьмич. <br>
<br>
В части - страшное смущенье, <br>
Непонятное волненье. <br>
Из леска, как на парад, <br>
Мчит немецкая машина, <br>
А на ней - что за причина? -<br>
Марш воинственно дудят: <br>
