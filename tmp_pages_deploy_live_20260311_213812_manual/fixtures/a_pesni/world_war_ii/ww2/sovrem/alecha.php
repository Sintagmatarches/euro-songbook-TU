&quot;Алеша&quot; - памятник советскому солдату под болгарским городом Пловдивом, 
сооруженный в 1952 году неподалеку от памятника героям Шипки - русским солдатам, 
павшим в русско-турецкую войну 1877-1878 гг. за независимость Болгарии. &quot;Алешей&quot; его назвали болгары по имени Алексея Скурлатова с Алтая, с которого был вылеплен памятник.<br>
