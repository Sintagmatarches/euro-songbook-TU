	<span class="header"><strong>БАЛЛАДА 
О МАТЕРИ</strong> <br></span> 
<br>
Музыка Евгения Мартынова<br>
  Слова Андрея Дементьева<br>
<br>
Постарела мать за тридцать лет,<br>
  А вестей от сына нет и нет.<br>
  Но она все продолжает ждать,<br>
  Потому что верит, потому что мать.<br>
<br>
И на что надеется она?<br>
  Много лет, как кончилась война.<br>
  Много лет, как все пришли назад,<br>
  Кроме мертвых, что в земле лежат.<br>
  Сколько их в то дальнее село,<br>
  Мальчиков безусых, не пришло.<br>
<br>
…Раз в село прислали по весне<br>
  Фильм документальный о войне.<br>
  Все пришли в кино – и стар, и мал.<br>
  Кто познал войну и кто не знал,<br>
  Перед горькой памятью людской<br>
  Разливалась ненависть рекой.<br>
<br>
Трудно было это вспоминать.<br>
  Вдруг с экрана сын взглянул на мать.<br>
  Мать узнала сына в тот же миг,<br>
  И пронесся материнский крик:<br>
  - Алексей! Алешенька! Сынок! –<br>
  Словно сын ее услышать мог.<br>
<br>
Он рванулся из траншеи в бой,<br>
  Встала мать прикрыть его собой.<br>
  Все боялась – вдруг он упадет,<br>
  Но сквозь годы мчался сын вперед.<br>
  - Алексей! – кричали земляки.<br>
  - Алексей! – просили. – Добеги!<br>
<br>
Кадр сменился. Сын остался жить,<br>
  Просит мать о сыне повторить.<br>
  И опять в атаку сын бежит.<br>
  Жив-здоров, не ранен, не убит.<br>
  - Алексей! Алешенька! Сынок! –<br>
  Словно сын ее услышать мог.<br>
<br>
Дома все ей чудилось кино…<br>
  Все ждала, вот-вот сейчас в окно<br>
  Посреди тревожной тишины<br>
  Постучится сын ее с войны.<br>
