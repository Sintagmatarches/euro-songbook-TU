<h1>ПОСЛЕДНИЙ БОЙ</h1>

Из кинофильма «Освобождение»<br>
<br>
Слова и музыка Михаила Ножкина<br>
<br>
Мы так давно, мы так давно не отдыхали.<br>
  Нам было просто не до отдыха с тобой.<br>
  Мы пол-Европы по-пластунски пропахали,<br>
  И завтра, завтра, наконец, последний бой.<br>
<br>
<span class='i'>Припев:</span><br>
<br>
Еще немного, еще чуть-чуть,<br>
  Последний бой – он трудный самый<br>
  А я в Россию, домой хочу,<br>
  Я так давно не видел маму!<br>
<br>
Четвертый год нам нет житья от этих фрицев,<br>
  Четвертый год соленый пот и кровь рекой.<br>
  А мне б в девчоночку в хорошую влюбиться,<br>
  А мне б до Родины дотронуться рукой.<br>
<br>
Последний раз сойдемся завтра в рукопашной,<br>
  Последний раз России сможем послужить,<br>
  А за нее и помереть совсем не страшно,<br>
  Хоть каждый все-таки надеется дожить!<br>
