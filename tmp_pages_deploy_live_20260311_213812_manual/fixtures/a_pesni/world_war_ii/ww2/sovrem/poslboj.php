&quot;Освобождение&quot; - пятисерийная киноэпопея Юрия Озерова о Великой Отечественной войне. Сценарий Юрия Бондарева и Оскара Курганова. Охватывает период от Курской битвы до победы: изображать первые годы войны запретили. Снималась с 1967 по 1971. Первые две серии (&quot;Огненная дуга&quot; и &quot;Прорыв) вышли на экраны в 1969, третья (&quot;Направление главного удара&quot;) - в начале 1971, заключительные (&quot;Битва за Берлин&quot; и &quot;Последний штурм&quot;) - в конце 1971. <br>
<br>
Песня &quot;Последний бой&quot; звучит в финальном фильме эпопеи, исполнил ее автор Михаил Ножкин, игравший лейтенанта Ярцева. <br>
