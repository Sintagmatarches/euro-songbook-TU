export function getHistoricalCardVisuals(country) {
  const meta = getHistoricalAffiliationMeta(country);
  const groupVisuals = meta?.group_key ? GROUP_VISUALS[meta.group_key] : null;
  const countryKey = meta?.country || String(country || "").trim();
  const countryVisuals = countryKey ? COUNTRY_VISUALS[countryKey] : null;
  return {
    ...DEFAULT_VISUALS,
    ...(groupVisuals || null),
    ...(countryVisuals || null),
  };
}
