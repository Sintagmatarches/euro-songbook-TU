export function getInitialLocale() {
  const raw = readStoredLocale() || DEFAULT_LOCALE;
  return LOCALES.includes(raw) ? raw : DEFAULT_LOCALE;
}

export function setLocale(locale) {
  const next = LOCALES.includes(locale) ? locale : DEFAULT_LOCALE;
  state.locale = next;
  persistLocale(next);
  document.documentElement.lang = next;
}

export function t(key, vars = {}) {
  const locale = LOCALES.includes(state.locale) ? state.locale : DEFAULT_LOCALE;
  const source = dict[locale] || dict[DEFAULT_LOCALE];
  const fallback = dict[DEFAULT_LOCALE] || {};
  let value = source[key] || fallback[key] || key;
  for (const [k, v] of Object.entries(vars)) {
    value = value.replaceAll(`{${k}}`, String(v));
  }
  return value;
}
